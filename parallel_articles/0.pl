Silesia ( czasopismo )
„ Silesia ” – niemieckojęzyczna gazeta wydawana w Cieszynie w latach 1862-1937 .
Była organem prasowym niemieckich liberałów na Śląsku Austriackim .
Historia .
Niemieccy liberałowie ( m.in .
Związek Ślązaków Austriackich ) wydawali w Opawie dla Śląska Opawskiego gazetę „ Troppauer Zeitung ” ( Gazetę Opawską ) a od 19 maja 1860 do 1861 w Cieszynie Karl Prochaska wydawał tygodnik „ Schlesischer Anzeiger ” ( Doniesienia Śląskie ) dla Śląska Cieszyńskiego .
Oba pisma zostały zastąpione przez wydawane od 4 stycznia 1862 pismo „ Silesia ” , której do 1878 redaktorem został Karl Prochaska .
Od 1873 wychodziła dwa razy w tygodniu , a od 1877 trzy razy w tygodniu , później ukazywała się codziennie do 1937 .
Była popularna wśród niemieckiego mieszczaństwa .
