.ac
.ac – krajowa domena internetowa najwyższego poziomu przypisana dla stron internetowych z Wyspy Wniebowstąpienia , aktywna od 1997 roku i administrowana przez NIC.AC .
Nazwa pochodzi od angielskiej nazwy wyspy `` Ascension Island '' .
Historia .
Domena dla Wyspy Wniebowstąpienia , administracyjnie będącej częścią brytyjskiego terytorium zamorskiego Wyspa Świętej Heleny , Wyspa Wniebowstąpienia i Tristan da Cunha , powstała w 1997 roku .
Użycie .
W styczniu 2010 w domenie .ac zarejestrowane były 244 strony .
Liczba ta w poprzedzających tygodniach była stabilna .
Liczba nowych stron oraz stron zamkniętych nie przekracza 10 tygodniowo .
