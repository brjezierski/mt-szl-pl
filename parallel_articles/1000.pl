Aigre
Aigre – miejscowość i gmina we Francji , w regionie Nowa Akwitania , w departamencie Charente .
W 2013 roku populacja ówczesnej gminy wynosiła 1104 mieszkańców .
Dnia 1 stycznia 2019 roku połączono dwie wcześniejsze gminy : Aigre oraz Villejésus .
Siedzibą gminy została miejscowość Aigre , a nowa gmina przyjęła jej nazwę .
