Rososz ( Białoruś )
Rososz ( ; ) – chutor na Białorusi , w obwodzie brzeskim , w rejonie baranowickim , w sielsowiecie Wolna .
W dwudziestoleciu międzywojennym folwark i osada Rososz leżały w Polsce , w województwie nowogródzkim , w powiecie baranowickim , do 22 stycznia 1926 w gminie Połoneczka , następnie w gminie Wolna .
