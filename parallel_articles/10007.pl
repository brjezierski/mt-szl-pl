Rostock
Rostock ( wym .
`` MAF : '' , ) , Hanse- und Universitätsstadt Rostock – miasto na prawach powiatu w kraju związkowym Meklemburgia-Pomorze Przednie w północno-wschodnich Niemczech .
Miasto to położone jest nad rzeką Warnow , w odległości ok. 12 km od jej ujścia do Zatoki Meklemburskiej na Morzu Bałtyckim .
Jest uznawany za jedyną w landzie tzw .
regiopolię ( `` Regiopole '' ) .
W czasach NRD Rostock był stolicą okręgu .
Liczy 208 886 mieszkańców .
Po Lubece i Kilonii jest trzecim co do wielkości niemieckim portem morskim nad Bałtykiem .
Jest jednocześnie największym ( ludnościowo i powierzchniowo ) miastem Meklemburgii-Pomorza Przedniego , chociaż nie jest jego stolicą ( jest nią Schwerin ) .
Toponimia .
Nazwa miasta pochodzi od połabskiego `` rostok '' , oznaczającego miejsce , w którym rozpływa się nurt rzeki .
