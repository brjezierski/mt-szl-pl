Aigueblanche
Aigueblanche – miejscowość i dawna gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Sabaudia .
W 2016 roku populacja ówczesnej gminy wynosiła 3292 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia trzech ówczesnych gmin – Aigueblanche , Le Bois oraz Saint-Oyen – powstała nowa gmina Grand-Aigueblanche .
Siedzibą gminy została miejscowość Aigueblanche .
