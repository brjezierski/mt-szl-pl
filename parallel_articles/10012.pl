Rothenburg ( Szwajcaria )
Rothenburg – miejscowość i gmina w środkowej Szwajcarii , w kantonie Lucerna , w okręgu Hochdorf .
Demografia .
W Rothenburgu mieszkają 7 792 osoby .
W 2021 roku 11,1 % populacji gminy stanowiły osoby urodzone poza Szwajcarią .
