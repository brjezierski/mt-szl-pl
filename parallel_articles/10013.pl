Rothesay ( Nowy Brunszwik )
Rothesay – miasto w Kanadzie , w prowincji Nowy Brunszwik , w hrabstwie Kings .
Według danych na rok 2020 miasto zamieszkiwało 12 129 osób , a gęstość zaludnienia wyniosłą 349,3 os./km2 .
