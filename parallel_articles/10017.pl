Rouen ( Cannan )
Rouen – wiersz angielskiej poetki May Wedderburn Cannan , należący do jej najpopularniejszych wierszy , będący zapisem przeżyć z czasów I wojny światowej , kiedy to poetka ( wykwalifikowana pielęgniarka Czerwonego Krzyża ) służyła jako wolontariuszka we Francji .
Wiersz został opublikowany w tomiku `` In War Time '' , wydanym w 1917 .
Utwór został włączony przez Philipa Larkina do antologii `` The Oxford Book of Twentieth Century English Verse '' .
Wiersz składa się z trzynastu strof czterowersowych .
Został opatrzony datami April 26 — May 25 , 1915 ( 26 kwietnia – 25 maja 1915 ) .
Muzykę do omawianego wiersza skomponował Chris O'Hara .
