Rougon
Rougon – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 77 osób , a gęstość zaludnienia wynosiła 2 osoby/km² .
W styczniu 2015 r. Rougon zamieszkiwały 103 osoby , przy gęstości zaludnienia wynoszącej 2,7 osób/km² .
