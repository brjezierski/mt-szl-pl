Rouyn-Noranda
Rouyn-Noranda – miasto w Kanadzie w prowincji Quebec , w regionie administracyjnym Abitibi-Témiscamingue .
Miasto nie należy do żadnej regionalnej gminy hrabstwa ( MRC ) i dlatego też ma status terytorium równoważnego z regionalną gminą hrabstwa ( TÉ ) .
Rouyn-Noranda powstało w rezultacie połączenia dwóch miast , Rouyn i Noranda , które zostały założone w 1926 roku nad brzegiem jeziora Osisko w wyniku odkrycia nieopodal bogatych zasobów miedzi w 1917 roku .
W wyniku reorganizacji podziału administracyjnego w Quebecu w 2002 r. , wszystkie gminy wchodzące w skład MRC Rouyn-Noranda połączyły się w jedno miasto .
Liczba mieszkańców Rouyn-Noranda wynosi 39 924 .
Język francuski jest językiem ojczystym dla 96,3 % , angielski dla 1,8 % mieszkańców ( 2006 ) .
W mieście rozwinął się przemysł drzewny oraz mleczarski .
