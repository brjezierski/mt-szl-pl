Roveredo
Roveredo ( hist .
`` Rofle '' , `` Ruffle '' ) – miejscowość i gmina w południowo-wschodniej Szwajcarii , w kantonie Gryzonia , siedziba administracyjna regionu Moesa .
Pod względem liczby mieszkańców jest największą gminą w regionie .
Demografia .
W Roveredo mieszka 2 597 osób .
W 2020 roku 24,8 % populacji gminy stanowiły osoby urodzone poza Szwajcarią .
Transport .
Przez teren gminy przebiegają autostrada A13 oraz droga główna nr 13 .
