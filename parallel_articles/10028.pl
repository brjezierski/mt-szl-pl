Rovinj
Rovinj ( wł .
`` Rovigno '' ) – miasto w Chorwacji , w żupanii istryjskiej , siedziba miasta Rovinj .
Leży nad Morzem Adriatyckim na półwyspie Istria .
W 2011 roku liczyło 13 056 mieszkańców .
Patronką miasta jest Eufemia z Chalcedonu .
Historia .
Historia Rovinja sięga VII wieku p.n.e. , był wówczas osadą plemienia Iliria .
W 129 p.n.e .
znalazł się pod panowaniem Imperium Rzymskiego .
Rzymianie miasto nazywali `` Arupiunum '' albo `` Mons Rubineus '' , później `` Ruginium '' i w końcu `` Ruvinium '' .
Zbudowano je na wyspie blisko wybrzeża .
