Rowan Atkinson
Rowan Sebastian Atkinson ( ur .
6 stycznia 1955 w Consett ) – brytyjski aktor , komik i scenarzysta , występował w roli Jasia Fasoli , którą zagrał w serialu BBC , oraz w dwóch pełnometrażowych filmach kinowych .
Wystąpił także m.in .
w rolach tytułowych w serialu `` Czarna Żmija '' oraz w filmach `` Johnny English '' , `` Johnny English Reaktywacja '' i `` '' .
Życiorys .
Pochodzenie i wykształcenie .
Jest najmłodszym z czwórki dzieci przedsiębiorcy z branży rolnej , jego starszy brat jest ekonomistą .
Rowan Atkinson jako dziecko jąkał się .
Z pomocą specjalistów udało mu się przezwyciężyć ten problem , ale jeszcze przez wiele lat szczególne trudności sprawiało mu wymawianie głoski „ B ” i nawet jako dorosły miał tendencję do jej przesadnego artykułowania , co potrafił zamienić w jeden z charakterystycznych elementów swoich wielu monologów i skeczy .
Uzyskał licencjat w zakresie elektrotechniki na University of Newcastle , a następnie został magistrem nauk ścisłych na Oxford University .
