Royal Tunbridge Wells
Royal Tunbridge Wells ( nazywane także Tunbridge Wells ) – miasto w Wielkiej Brytanii , w Anglii , w hrabstwie Kent w pobliżu granicy z hrabstwem East Sussex .
W 2001 roku miasto liczyło 60 095 mieszkańców .
Jest centrum administracyjnym dla dystryktu ( `` borough '' ) Tunbridge Wells .
W mieście znajduje się stacja kolejowa Tunbridge Wells .
