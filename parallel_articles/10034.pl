Czarnostopka kasztanowa
Czarnostopka kasztanowa ( `` Picipes badius '' ( Pers . )
Zmitr .
& amp ; Kovalenko ) – gatunek grzybów należący do rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Picipes , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisany został w 1801 r. przez Christiana Hendrika Persoona jako `` Boletus badius '' , później przeważnie zaliczany był do rodzaju `` Polyporus '' ( żagiew ) .
Obecną , uznaną przez Index Fungorum nazwę , nadali mu Ivan V. Zmitrovich i Alexander E. Kovalenko w 2016 roku , przenosząc takson do nowo utworzonego rodzaju `` Picipes . ''
Synonimy naukowe : Nazwa polska na podstawie rekomendacji Komisji ds .
Polskiego Nazewnictwa Grzybów .
Jest ona spójna z nazwą naukową zaakceptowaną przez Index Fungorum .
Uprzednio , Władysław Wojewoda w 2003 r. zaproponował nazwę żagiew kasztanowa , gdyż wtedy takson ten należał do rodzaju `` Polyporus '' .
W polskim piśmiennictwie mykologicznym gatunek ten opisywany był wcześniej przez Franciszka Błońskiego jako żagiew czarnotrzonowa i huba czarnotrzonowa .
Morfologia .
Początkowo podwinięty , później rozpostarty , w końcu lejkowaty .
