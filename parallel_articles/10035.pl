Strój rozbarski
Strój rozbarski ( bytomski , górzański ) – jeden z typów strojów ludowych z Górnego Śląska .
Śląskie stroje różnią się w zależności od regionu , a nawet poszczególnych miast i wsi , w jakich były noszone .
Tereny występowania elementów wzajemnie przenikały się wraz z przemieszczaniem się ludności w XIX i XX wieku .
Szczególnie kobiety – mieszkanki Śląska zaczęły także dostosowywać swoje stroje do mody miejskiej , co także zmieniało wygląd stroju .
