Modelowanie sylwetki .
W celu uzyskania `` kopiastej '' sylwetki , podobnej do krynoliny , Ślązaczki stosowały różne rozwiązania .
Dla poszerzenia sylwetki oraz dla utrzymania ciężaru kolejnych warstw stroju , stosowano `` kiełbaśnice '' lub `` watówki '' .
Pod suknię wierzchnią zakładano też po kilka spódnic .
Prócz płóciennych halek , używano spódnice uszyte z różnych kolorowych tkanin , a ich nazwy były związane z rodzajem użytego materiału .
Niektóre z nich , w mniej uroczystych okolicznościach , były wykorzystywane jako spódnice wierzchnie .
Strój męski .
Strój mężczyzny nazywa się `` ancugiem '' , nazwa ta kojarzona jest głównie z garniturem .
Jednak niewielu ludzi pamięta , że w gwarze śląskiej słowo `` ancug '' oznaczało ogólnie zarówno komplet męski jak i żeński .
Męski strój rozbarski składał się z ciemnej sukiennej `` kamuzeli '' ( marynarki ) , `` brzuśleka '' ( kamizelki ) , koszuli , `` jedbowki '' ( chustki wiązanej wokół szyi ) , spodni `` jelynioków '' i butów `` kropów '' lub spodni `` bizoków '' i butów `` sztybletów '' .
Efekty wieńczyło nakrycie głowy .
