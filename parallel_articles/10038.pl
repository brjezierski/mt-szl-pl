Rozdzielna
Rozdzielna ( ukr .
Роздільна , `` Rozdilna '' ) – miasto na Ukrainie w obwodzie odeskim , siedziba władz rejonu rozdzielniańskiego , liczy 17 858 mieszkańców ( 2019 ) .
Ośrodek przemysłu spożywczego .
Historycznie położone w Jedysanie .
Od 1922 miejscowość leżała w granicach ZSRR .
W latach 1941–1944 była pod zarządem Królestwo Rumunii jako część Transnistrii .
