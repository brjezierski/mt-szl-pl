Rozdziele ( Beskid Niski )
Rozdziele ( 789 m n.p.m .
; dawniej również : Cyrhla , 790 m n.p.m . )
– niewybitny szczyt na południowo-zachodnim skraju Beskidu Niskiego , wznoszący się na północ od wsi Mochnaczka Wyżna , ok. 2 km na wschód od przełęczy Krzyżówka .
Leży w grzbiecie , stanowiącym wododział między dorzeczem Mochnaczki ( a więc Popradu ) na południowym zachodzie a dorzeczem Białej Dunajcowej na północnym wschodzie .
Grzbiet ten , oddzielający się od głównego grzbietu wododziałowego Karpat na Czerteży ( tuż na wschód od Dzielca , 792 m n.p.m . )
biegnie na północny zachód przez Mizarne ( 770 m n.p.m . )
i Piorun ( 743 m n.p.m .
) , po czym w szczycie Harniaków Wierchu ( 768 m n.p.m . )
zakręca ku zachodowi , by przez Rozdziele i Garb ( 780 m n.p.m. , dawniej 784 m n.p.m . )
osiągnąć przełęcz Krzyżówkę , za którą ponownie wznosi się i rozgałęzia , tworząc pasmo Jaworzyny Krynickiej .
Najwyższy punkt wzniesienia leży nieco na południe od linii wspomnianego grzbietu .
Na zachodnich i południowych stokach szczytowego spiętrzenia Rozdziela mają swój początek niektóre źródłowe potoki Mochnaczki , natomiast na stokach wschodnich – źródłowe cieki jej lewobrzeżnego dopływu – Fataloszki .
Szczyt prawie w całości zalesiony , jedynie na południowo-wschodnich zboczach ciąg polan .
W całym masywie Rozdziela i Harniaków Wierchu nie ma znakowanych szlaków turystycznych co powoduje , że jest on bardzo rzadko odwiedzany przez turystów .
