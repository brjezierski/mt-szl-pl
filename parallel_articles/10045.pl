Rozwadza
Rozwadza ( niem .
`` Annengrund '' ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Zdzieszowice .
Od 1950 r. miejscowość administracyjnie należy do województwa opolskiego .
Nazwa .
Po raz pierwszy nazwę zanotowano w 1302 roku w bulli papieża Bonifacego VIII jako `` Rosvatzha '' lub `` Rozwadze '' .
Papież nałożył na wieś Rosvatzha dziesięcinę na rzecz klasztoru Cystersów w Gemelnitz ( dawna nazwa dzisiejszej Jemielnicy ) .
Nazwa pojawia się także w kronice cystersów z Jemielnicy z 1282 roku .
Nazwa miejscowości pochodzi najprawdopodobniej od staropolskiego imienia Rozwad .
W alfabetycznym spisie miejscowości z terenu Śląska wydanym w 1830 roku we Wrocławiu przez Johanna Knie miejscowość występuje pod obecnie używaną polską nazwą `` Rozwadza '' oraz niemiecką `` Rozwadze '' .
W okresie narodowego socjalizmu w latach 1936-1945 w ramach akcji germanizacji nazw zmieniono nazwę miejscowości na nową , niemiecką `` Annengrund '' .
