Rozwój Katowice
Rozwój Katowice – polski klub piłkarski z Katowic grający w IV lidze .
Historia .
Klub wywodzi się z Towarzystwa Gimnastycznego „ Sokół ” Katowice-Brynów założonego w 1919 roku .
27 listopada 1925 roku klub zarejestrowano w Górnośląskim Związku Okręgowym Piłki Nożnej , toteż jest to oficjalna data powstania klubu .
Pierwszym prezesem klubu był Robert Neuman .
Założycielami klubu byli pracownicy kopalni Wujek wraz z rodzinami : Emanuel Wakerman , Robert Neuman , Alfred Hatko , Franciszek Student , Wilhelm Kwiatkowski , Wincenty Poloczek , Franciszek Kluzik .
26 stycznia 1927 roku w Okręgowym Związku Bokserskim zarejestrowano sekcję bokserską , co uczyniło z klubu Rozwój Katowice klub wielosekcyjny .
Z biegiem lat w klubie powstawały kolejne sekcje sportowe : w 1927 – sekcja siatkówki , w 1930 – sekcja gimnastyczna , w 1954 – sekcja tenisa stołowego , w 1960 – sekcja rugby , w 1962 – sekcja lekkoatletyczna , w 1963 – sekcja piłki ręcznej , w 1971 – sekcja koszykówki , w 1984 – sekcja gimnastyki artystycznej i sekcja szachowa .
W 1939 roku Rozwój Katowice awansował do A-klasy , lecz w wyniku wybuchu II wojny światowej klub zawieszono .
Reaktywowano go po wojnie we wrześniu 1946 roku .
W sezonie 1947-1948 klub zgłoszono do rozgrywek C-klasy .
W latach 1949–1952 w wyniku fuzji z K.S .
Ligocianką Ligota zmiana nazwy na ZKS GÓRNIK Katowice-Ligota .
W 1957 roku drużyna tenisa stołowego awansowała do klasy „ A ” .
