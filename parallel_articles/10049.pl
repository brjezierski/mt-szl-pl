Rościęcino
Rościęcino – wieś w Polsce położona w województwie zachodniopomorskim , w powiecie kołobrzeskim , w gminie Kołobrzeg przy drodze wojewódzkiej nr 162 .
Wieś leży nad rzeką Parsętą ok. 8-9 km od jej ujścia do Bałtyku .
Położenie .
Większa część wsi zlokalizowana została wzdłuż drogi lokalnej .
Wartości kulturowe :
Na południe od wsi ujęcie wody z zabytkowymi zabudowaniami z 1905 r. ( Kopydłówko ) oraz średniowieczne grodzisko .
Gospodarka .
Obiekty hodowlane i gospodarcze wielkoobszarowych gospodarstw rolnych ( byłego PGR-u ) - wykorzystanie częściowe .
