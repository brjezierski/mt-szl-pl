Rożniatów ( Ukraina )
Rożniatów ( ukr .
Рожнятів , `` Rożniatiw '' ) – osiedle typu miejskiego na Ukrainie , stolica rejonu w obwodzie iwanofrankiwskim .
Historia .
Do roku 1914 w powiecie bolechowskim , w austriackiej prowincji ( kraj koronny ) Królestwa Galicji i Lodomerii .
Pod okupacją hitlerowską wchodził w skład gminy Rożniatów .
W 1989 liczyło 4259 mieszkańców .
Zamek .
Pierwsze wzmianki o Rożniatowie pochodzą z 1464 r. W XV w. w mieście został zbudowany zamek warowny , który posiadał dwie wieże oraz dwa tunele do uroczyska Baturin i do klasztoru oo .
