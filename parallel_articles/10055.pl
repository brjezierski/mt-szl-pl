Rožnov pod Radhoštěm ( stacja kolejowa )
Rožnov pod Radhoštěm – stacja kolejowa w Rožnovie pod Radhoštěm , w kraju zlińskim , w Czechach .
Jest stacją końcową linii z Uherské Hradiště .
Znajduje się na wysokości 375 m n.p.m .
Na stacji istnieje możliwość zakupu biletów na wszystkiego rodzaju pociągi oraz rezerwacji miejsc .
