Maślaczek rubinowy
Maślaczek rubinowy , rubinoborowik dębowy ( `` Chalciporus rubinus '' ( W.G .
Sm . )
Singer ) – gatunek grzybów z rodziny borowikowatych ( `` Boletaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chalciporus , Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten opisał w 1868 roku Worthington George Smith , nadając mu nazwę `` Boletus rubinus '' .
W 1969 r. Albert Pilát i Aurel Dermek przenieśli go do rodzaju `` Rubinoboletus '' , a w 1973 r. Rolf Singer do rodzaju `` Chalciporus '' .
Synonimy :
Komisja ds .
Polskiego Nazewnictwa Grzybów przy Polskim Towarzystwie Mykologicznym w 2021 r. zarekomendowała dla `` Rubinoboletus rubinus '' polską nazwę rubinoborowik dębowy .
W 2022 r. nazwa ta stała się niespójna z aktualną nazwą naukową .
W internetowym portalu grzybiarzy gatunek ten opisywany jest pod nazwą maślaczek rubinowy `` Chalciporus rubinus '' i nazwa ta jest spójna z nazwą naukową .
Morfologia .
O średnicy 1,4–8,5 cm , w młodych okazach półkulisty z garbkiem , potem płasko wypukły , w końcu płaski .
