Ajaccio
Ajaccio ( fr .
`` Ajaccio '' [ ] ; kors .
`` Aiacciu '' [ ] ; wł .
`` Ajaccio '' [ ] ; łac .
`` Adiacium '' ) – miasto i gmina we Francji , w regionie Korsyka , w departamencie Korsyka Południowa .
Leży na zachodnim wybrzeżu Korsyki , nad Morzem Śródziemnym .
Jest ośrodkiem administracyjnym regionu Korsyka .
Niewielki port handlowy oraz porty : rybacki ( połów sardynek ) , promowy ( linie do Marsylii , Tulonu i Nicei ) i jachtowy .
Stocznia .
Około 1 km na wschód od miasta znajduje się Port lotniczy Ajaccio-Campo dell'Oro .
Ośrodek turystyczny i uzdrowisko klimatyczne .
Ważniejsze zabytki : Katedra ( XIV w. ) , kościół Saint-Erasme ( XVII w. ) , cytadela ( XVI w. ) , dom rodzinny cesarza Napoleona I , który tu urodził się 15 sierpnia 1769 .
Ajaccio jest również siedzibą francuskiego klubu piłki nożnej AC Ajaccio .
Według danych na rok 2018 gminę zamieszkiwało 71 685 osób , a gęstość zaludnienia wynosiła 874 osób/km² .
