Rudolfin
Rudolfin – wieś w Polsce położona w województwie lubelskim , w powiecie chełmskim , w gminie Kamień .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 200 mieszkańców i była siódmą co do wielkości miejscowością gminy Kamień .
