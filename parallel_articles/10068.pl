Rudy-Rysie
Rudy-Rysie – wieś w Polsce położona w województwie małopolskim , w powiecie brzeskim , w gminie Szczurowa , przy DW768 .
Rudy-Rysie powstały w wyniku połączenia dwóch niegdyś oddzielnych wsi .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnowskiego .
Integralne części miejscowości : Rudy Drugie , Rudy Pierwsze , Sołtysie .
Pierwszy człon nazwy nie odmienia się , np .
w Rudy-Rysiu , z Rudy-Rysia .
Według `` Słownika Historyczno-Geograficznego Ziem polskich w Średniowieczu '' w roku 1396 – pierwszy raz pojawia się wzmianka o osadzie Rudy .
W 1416 r. obok nazwy Rudy pojawia się człon Kuźnica ( nazwa sugerująca rudę i kuźnica sugeruje związek tych terenów z wydobyciem i przetwarzaniem rudy darniowej , tworzącej się na podmokłych łąkach , przez które wije się koryto rzeki ) .
