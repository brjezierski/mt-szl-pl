AFC Ajax ( piłka nożna kobiet )
Kobieca sekcja holenderskiego klubu piłkarskiego AFC Ajax , utworzona 18 maja 2012 roku .
W pierwszym sezonie ( 2012/2013 ) piłkarki Ajaksu zagrały w nowo powstałej , transgranicznej lidze Belgii i Holandii ( BeNe League ) .
Stadionem na którym amsterdamki rozgrywają swoje mecze domowe jest Sportpark De Toekomst .
