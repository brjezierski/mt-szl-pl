Rudzka Kuźnica
Rudzka Kuźnica ( niem .
`` Rudahammer '' ) − to północna część dzielnicy Ruda w Rudzie Śląskiej .
Rudzka Kuźnica leży na północ od rzeki Bytomka , granicząc na zachód z Zabrzem i od północy z Bytomiem .
W Rudzkiej Kuźnicy znajdują się trzy ulice : ul .
Jana III Sobieskiego ( w 1683 wojska Sobieskiego maszerowały przez Kuźnicę na Wiedeń ) , ul .
Nad Bytomką i część ulicy Piastowskiej .
Kuźnica była zamieszkiwana głównie w budynkach dwukondygnacyjnych ( tzw .
`` familoki '' ) przez robotników .
Historia osiedla .
Tereny dzisiejszej Rudzkiej Kuźnicy , stanowiły w XII wieku część Czarnego Lasu Bytomskiego .
Pierwsza wzmianka o Kuźnicy pochodzi z 1401 .
W 1644 kościół biskupicki wprowadził księgi metrykalne , w których rejestrowani byli rudzianie .
W roku 1683 przez Kuźnice maszerowały wojska Jana III Sobieskiego , podążając pod Wiedeń .
Na mapie Homanna z 1736 roku jest wymieniona osada Kuźnica , zaś na mapie Bytomki z lat 1788 - 1789 figuruje jako Ruder Kuznitze .
Jednym z pierwszych zakładów przemysłowych była
wzmiankowana w 1642 r. kuźnica żelaza , od której pochodzi nazwa obecnej kolonii Kuźnica Rudzka ( RUDAHAMMER ) .
W latach 1812 - 1816 wybudowano tu nowoczesną hutę cynku `` Carlshütte '' ( Huta Karol ) .
Była ona w owym czasie największa na Śląsku i w całej wschodniej Europie .
Około 1900 roku , została zbudowana przez hrabiego Ballestrema zbudowano w dolnej części Kuźnicy `` familoki '' , po południowej stronie ulicy Jana III Sobieskiego , za nim jeszcze zbudowany został szpital Spółki Brackiej - dawniejszego Knappschaftu .
Dla odciążenia szpitali Bytomia i Zabrza zbudowano w latach 1904–1908 w Rudzkiej Kuźnicy na terenie 45 725 m² przyległym do szosy między Bobrkiem a Biskupicami Szpital Spółki Brackiej ( Knappschafts - Lazarett Rudahammer ) .
Na owe czasy był to jeden z największych i najnowocześniejszych szpitali na Górnym Śląsku .
Dla nowego lazaretu w Rudahammmer został w roku 1904 zakupiony grunt o powierzchni 45 725 metrów kwadratowych przy szosie z Bobrka do Borsigwerk ( obecne dzielnica Zabrza ) .
W roku 1907 wzniesiono mury otaczające i płoty oraz portiernię jako biuro robót budowlanych .
Rozpoczęcie budowy całego zakładu opóźniło się aż do wiosny 1908 roku , z powodu protestu przeciwko osiedlaniu się na podstawie prawa górniczego .
Zakład został oddany do użytku w 1910 roku .
Obiekt oddano do użytku w październiku 1910 roku .
W okresie I wojny światowej pełnił funkcję niemieckiego szpitala wojskowego .
Wskutek rabunkowej gospodarki kopalń w latach międzywojennych i 1945 , należących do koncernu Godulla , teren na którym stoi szpital , został odebrany .
Gmach szpitala i budynki administracyjne zaczęły się zapadać i szpital należało zlikwidować .
W miejscu gdzie kiedyś stał gmach główny szpitala dziś jest boisko do gry w piłkę .
W latach 1898–1929 przez ul .
Sobieskiego ( niem .
`` Borsigwerkerstrasse '' ) jeździł tramwaj .
W 1922 w wyniku wytyczenia nowej granicy polsko-niemieckiej Rudzka Kuźnica znalazła się w Polsce , ale otoczona z trzech stron terytorium niemieckim .
Niemieckie tramwaje kursujące między Zabrzem a Bytomiem jeździły tutaj w ramach tzw .
uprzywilejowanego tranzytu - bez zatrzymywania się na polskiej ziemi .
W 1929 strona niemiecka zbudowała drogową i tramwajową obwodnicę miejscowości i przestała korzystać z torów po polskiej stronie .
Park im .
Jana III Sobieskiego .
Park w Rudzkiej Kuźnicy założony został w drugiej połowie XIX wieku przez hrabiego Ballestrema .
W parku była restauracja z salą taneczną .
W budynku tym ponadto mieścił się sklep - konsum , który do 1945 roku prowadziła p.Winkler .
Zaopatrywała się w nim większa część mieszkańców .
Park do lat pięćdziesiątych XX wieku stanowił miejsce rekreacji - niedzielnego odpoczynku .
Urządzano tam bowiem koncerty i inne imprezy .
W części przyległej do restauracji ustawione stoły i ławki na betonowych stojakach .
Na alejkach i kwietnikach parkowych zawsze panował porządek , gdyż ludzie szanowali dobro
pozostające do ich dyspozycji .
Dolna część pasku , obsadzona akacjami stwarzała ludziom możliwość odpoczynku na trawie w cieniu drzew .
Z upływem lat jednak park był coraz bardziej
dewastowany .
W południowej części budynku , która została odrestaurowana , urządzono restaurację i salę gier .
Kiedyś restauracja , biblioteka ( lata 60 , 70 , początek lat 80 XX wieku ) a potem założono w miejscu biblioteki bar `` pod kasztanami '' , którego właścicielem był Pańczyk .
Obiekt ten od 2007 roku nie istnieje .
W latach 70 XX wieku w parku znajdował się amfiteatr .
Park jest obecnie w dużej mierze porośnięty lasem , a w nim pozostało jedynie boisko do piłki nożnej i koszykówki , stół do tenisa i plac zabaw .
Charakterystyczny dla Rudzkiej Kuźnicy jest Staw Szkopka , który jest objęty sekcją wędkarską ( dzierżawiony przez Polskie Koło Wędkarskie - PZW-Okręg Katowice ) .
Powierzchnia całkowita stawu to około 8107 m² .
Staw ten jest pochodzenia antropogenicznego .
Zarybiany jest przez koła wędkarskie .
Wśród łowionych gatunków ryb są : karpie , płocie , amury , szczupak i wiele innych gatunków .
W stawie można nie tylko spotkać ryby , ale też żaby zielone i wodne , ropuchy szare oraz ptaki lęgowe : kaczkę krzyżówki , kokoszki wodne .
Tereny stawu Szkopka stanowią malownicze zakątki , pośród których rozkwitają różne formacje roślinne , a przedstawiciele różnych gatunków zwierząt wybierają je na miejsca lęgowe .
Najczęstszą formacją roślinną są szuwary i trzcinowiska .
Często w otoczeniu szkopki występują zarośla wierzbowe składające się z wierzb płaczących , białych i kruchych oraz zwykłe drzewa liściaste .
Park im .
Jana III Sobieskiego nie jest objęty ochroną Urzędu Miejskiego i należy do zieleni nieurządzonej .
Aktualnie park ma powierzchnię ok. 3 ha .
W parku występuje wiele ciekawych drzew .
Najczęściej są to kasztanowce zwyczajne , lipy drobnolistne i szerokolistne , graby pospolite , buki zwyczajne , dęby szypułkowe i wierzby białe .
Wśród krzewów spotyka się bez czarny , śnieguliczkę .
Wiele drzew liczy sobie 100-120 lat .
Jedno z drzew ma w obwodzie 300 cm i jest jednym z najgrubszych w parkach Górnego Śląska .
Jest tam także kilkanaście starych okazów grabu pospolitego .
Okazały jest także dąb burgundzki , który ma wysokość ponad 20 metrów liczy ponad 100 lat .
