Rugby ( miasto )
Rugby ( wym . )
– miasto w środkowej Anglii we wschodniej części hrabstwa Warwickshire , w dystrykcie Rugby , położone nad rzeką Avon , 21 km na wschód od Coventry , niedaleko granicy z hrabstwami Northamptonshire ( od wschodu ) i Leicestershire ( od północnego wschodu ) .
W 2001 roku miasto liczyło 61 988 mieszkańców .
Rugby znane jest głównie z wynalezienia gry o tej samej nazwie co miasto .
Obecnie w rugby gra się na całym świecie .
Legenda mówi , że grę stworzył William Webb Ellis w 1823 w Rugby School .
Miasto jest także miejscem narodzin silnika odrzutowego .
W kwietniu 1937 r. Frank Whittle wybudował pierwszy na świecie prototyp silnika odrzutowego w zakładach British Thomson- Houston w Rugby , a w latach 1936–1941 bazował w Brownsover Hall na obrzeżach Rugby , gdzie projektował i rozbudowywał wczesne prototypy silników .
