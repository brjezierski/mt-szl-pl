Ruhla
Ruhla – miasto uzdrowiskowe w Niemczech , w kraju związkowym Turyngia , w powiecie Wartburg , w Lesie Turyńskim .
Liczba mieszkańców w 2009 wynosiła 6 210 .
Miasto pełni funkcję `` gminy realizującej '' ( niem .
`` erfüllende Gemeinde '' ) dla gminy wiejskiej Seebach .
W mieście rozwinął się przemysł precyzyjny , maszynowy , elektrotechniczny oraz drzewny .
