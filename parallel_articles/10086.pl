Ruse
Ruse ( ; ; ) – miasto w Bułgarii , nad Dunajem , piąte pod względem liczby mieszkańców w kraju .
Stolica obwodu Ruse i gminy Ruse .
Według danych szacunkowych Ujednoliconego Systemu Ewidencji Ludności oraz Usług Administracyjnych dla Ludności , 15 grudnia 2018 roku miejscowość liczyła 152 787 mieszkańców .
Największy bułgarski port rzeczny na Dunaju , przez który przechodzi znaczna część bułgarskiego eksportu .
Jest to także ważny węzeł komunikacyjny , ponieważ poprzez Most Przyjaźni możliwe są połączenia kołowe i kolejowe z Rumunią .
Rozwinięty przemysł środków transportu ( budowa statków , taboru kolejowego ) , maszynowy ( m.in .
obrabiarki , maszyny rolnicze ) , metalowy , spożywczy ( m.in .
przetwórstwo owoców i warzyw , winiarstwo , przemysł cukrowniczy , tytoniowy , młynarski ) , włókienniczy ( bawełniany i jedwabniczy ) , chemiczny , rafinacja ropy naftowej ; poza tym przemysł skórzano-obuwniczy , drzewny , gumowy .
Ruch lotniczy obsługuje port lotniczy Ruse .
Miasto jest znane ze swojej XIX i XX-wiecznej architektury .
