Jesiotr rosyjski
Jesiotr rosyjski , jesiotr wschodni ( `` Acipenser gueldenstaedtii '' ) – gatunek ryby z rodziny jesiotrowatych ( Acipenseridae ) .
Poławiany gospodarczo na dużą skalę .
Występowanie .
Żyje w przybrzeżnych wodach Morza Czarnego , Morza Azowskiego i Morza Kaspijskiego .
W niektórych rzekach ( Wołga i Dunaj ) tworzy prawdopodobnie formę wyłącznie słodkowodną .
W latach 2000–2001 w Bałtyku złowiono dwa osobniki nieznanego pochodzenia .
Budowa .
Osiąga zwykle od 130 do 250 cm ( ale zdarzają się okazy 4-metrowe ) , waży zwykle ok. 20–30 kg , rekordowe okazy do ok. 160 kg .
Cechą charakterystyczną jesiotra rosyjskiego jest stosunkowo krótki ryj ( `` rostrum '' ) i przerwana warga dolna .
Wąsiki krótkie .
Ciało jest znacznie wyższe niż u pozostałych jesiotrów .
Odżywianie .
Żywi się bezkręgowcami i rybami .
Rozród .
Wiosną wpływa do górnego biegu rzek , aby odbyć tarło ( kwiecień-czerwiec ) w głębokiej wodzie na piaszczystym lub żwirowym podłożu .
Znaczenie gospodarcze .
Ryba poławiana na dużą skalę ze względu na smaczne mięso oraz ikrę , z której wyrabiany jest kawior ( największy udział w połowach ryb jesiotrowatych ) .
Hodowana w gospodarstwach rybnych .
Zagrożenia .
Gatunek wpisany do Czerwonej Księgi IUCN jako krytycznie zagrożony wyginięciem ( kategoria CR ) na całym obszarze naturalnego występowania .
