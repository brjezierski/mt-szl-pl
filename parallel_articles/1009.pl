Wiadro
Wiadro ( inaczej kubeł ) – naczynie w kształcie walca lub odwróconego , ściętego stożka , dawniej najczęściej blaszane , obecnie z tworzywa sztucznego , zaopatrzone w ruchomy , kabłąkowaty uchwyt .
Używane jest zazwyczaj jako pojemnik na ciecze lub materiały sypkie , głównie do ich ręcznego transportu .
Pojemność typowego wiadra to około od 10 do 15 litrów , rzadziej spotyka się wiadra o mniejszej i większej pojemności .
Metalowe wiadra wykonywane są z blachy stalowej jako ocynkowane albo emaliowane .
Wiadro może mieć na dole pierścień z blachy zwany stopką .
Pałąk może być przewleczony przez drewnianą rączkę .
Małe wiaderka o pojemności około 2 litrów są powszechnie stosowaną zabawką dziecięcą do piaskownicy .
Plastikowe wiadra i wiaderka ( z atestem spożywczym ) znajdują zastosowanie w przemyśle spożywczym , gdzie służą do przechowywania i sprzedaży produktów spożywczych .
Wyposażone są wtedy w pokrywki oraz często w plomby zabezpieczające .
Kubłem zwane jest też urządzenie do pogłębiania szybów górniczych .
