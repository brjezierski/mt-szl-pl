Kobylnica Ruska
Kobylnica Ruska ( ) , w latach 1977–1981 Kopytów Dolny – wieś w Polsce położona w województwie podkarpackim , w powiecie lubaczowskim , w gminie Wielkie Oczy .
W latach 1975–1998 miejscowość administracyjnie należała do województwa przemyskiego .
Części wsi .
Wieś starostwa niegrodowego lubaczowskiego na początku XVIII wieku .
Wierni Kościoła rzymskokatolickiego należą do parafii Trójcy Przenajświętszej w Potoku Jaworowskim .
W Kobylnicy Ruskiej znajdowała się też cerkiew greckokatolicka , która została rozebrana w 1954 roku .
