Gołąbek ametystowy
Gołąbek ametystowy ( `` Russula amethystina '' Quél . )
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Russula , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podała Alina Skirgiełło w 1991 r .
Morfologia .
Średnica 5-11 cm , początkowo wypukły , potem rozpostarty , na koniec wklęsły .
Brzeg gładki tępy , u starszych okazów karbowany .
Powierzchnia gładka , naga , w stanie wilgotnym lepka i błyszcząca , w stanie suchym matowa i jak gdyby ziarenkowata , o barwie liliowej , ametystowej , fioletowoliliowej , czerwonawej .
Środek ciemniejszy , brązowawy .
Skórkę można zedrzeć do połowy
