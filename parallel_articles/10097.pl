Gołąbek komorowaty
Gołąbek komorowaty ( `` Russula cavipes '' Britzelm . )
– gatunek grzyba z rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Russula , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podała Alina Skirgiełło w 1991 r. Synonimy naukowe :
Morfologia .
Średnica 2,5-9 cm , początkowo wypukły , potem rozpostarty , czasami z małym garbkiem .
Brzeg cienki , dość tępy , gładki lub karbowany .
Skórka bardzo cienka i gładka .
Podczas wilgotnej pogody lepka , podczas suchej błyszcząca .
Daje się z kapelusza ściągnąć całkowicie .
Ma kolor liliowy , różowoliliowy lub błękitnoliliowy , na środku brązowooliwkowy z purpurowobrązowymi , żółtawymi lub szarozielonkawymi plamami .
Na ogół jest to gołąbek o drobnym kapeluszu ( do 6 cm średnicy ) i zmiennym ubarwieniu – spotyka się także formy całkowicie fioletowe
U młodych okazów gęste , potem rzadkie .
