Gołąbkowate
Gołąbkowate ( `` Russulaceae '' Lotsy ) – rodzina grzybów z rzędu gołąbkowców ( `` Russulales '' ) .
Charakterystyka .
Rodzina `` Russulaceae '' zawiera gatunki naziemnych grzybów wytwarzających owocniki mięsiste o hymenoforze blaszkowym .
U niektórych miąższ po przełamaniu przebarwia się .
Zarodniki gołąbkowatych są brodawkowane , a ich wysyp barwy białej , kremowej lub żółtej .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
