Gołąbek piekący
Gołąbek piekący ( `` Russula sororia '' Fr . )
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' )
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Russula , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podał Władysław Wojewoda w 2003 r. , Alina Skirgiełło w 1991 r. opisywała ten gatunek pod nazwą gołąbek siostrzany .
Synonimy łacińskie :
Morfologia .
Mięsisty o barwie ciemnoochrowej lub brązowoszarej .
Wypukły z wiekiem wklęsły .
Średnica kapelusza od 5 do 12 cm .
Skórka lepka .
