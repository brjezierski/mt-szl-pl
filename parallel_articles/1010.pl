Lane kluski
Lane kluski ( lane ciasto ) – rodzaj klusek przygotowanych poprzez wlewanie surowego ciasta do gotującego się płynu .
Może to być woda , mleko lub zupa , w której będą podane .
Składniki wchodzące w skład ciasta na lane kluski to : jajka , mąka , woda i sól do smaku .
Wszystkie składniki miesza się , ubijając .
Konsystencja uzyskanej masy powinna być na tyle rzadka , by możliwe było wylewanie jej z naczynia z dzióbkiem .
Tak przygotowane ciasto wlewa się powoli , cienkim strumieniem do gotującego się płynu .
W kuchni polskiej , lane kluski zwykle stanowią dodatek do zup takich jak zupa mleczna ( tzw .
lane kluski na mleku ) , pomidorowa lub rosół .
