Rutherglen
Rutherglen ( gael .
`` An Ruadh-Ghleann '' ) – miasto w środkowej Szkocji , w hrabstwie South Lanarkshire , położone na południowym brzegu rzeki Clyde , w aglomeracji Glasgow .
W 2011 roku liczyło 31 401 mieszkańców .
Miasto założone zostało w 1126 roku na mocy edyktu króla Dawida I jako jedno z pierwszych `` royal burghs '' , wkrótce stając się ważnym ośrodkiem handlu .
W XIII wieku zbudowany został tu zamek , kilkakrotnie będący świadkiem zmagań Szkotów i Anglików podczas I wojny o niepodległość Szkocji .
Zamek został doszczętnie spalony podczas wojny domowej w 1569 roku , z polecenia regenta Szkocji Jamesa Stewarta .
Ostatnie ślady po zamku zostały zniszczone w XVIII wieku .
W przeszłości miasto było ośrodkiem włókiennictwa , wydobycia węgla i hutnictwa żelaza .
W XIX wieku funkcjonowała tutaj stocznia .
