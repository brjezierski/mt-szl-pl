Język rutulski
Język rutulski ( nazwa własna : МыхIабишды чIел ) należy do rodziny języków kaukaskich .
Używany jest przez Rutulów ( ok. 17,8 tys .
osób ) , rdzenną ludność kaukaską , zamieszkującą południowy Dagestan i częściowo Azerbejdżan .
Wyróżnia się pięć dialektów języka rutulskiego .
Język rutulski nie wykształcił normy literackiej , choć w ostatnim czasie prowadzi się próby stworzenia alfabetu opartego na cyrylicy i zapoczątkowania piśmiennictwa rutulskiego .
