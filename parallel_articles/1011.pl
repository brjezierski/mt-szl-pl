Sopel ( zestaw przeciwlotniczy )
Sopel – polski prototypowy , samobieżny zestaw przeciwlotniczy artyleryjsko-rakietowy , opracowany w latach 90 .
XX wieku .
Historia .
W latach 70. rozpoczęto w Polsce produkcję licencyjną działa samobieżnego 2S1 Goździk .
Równocześnie rozpoczęto produkcje transportera opancerzonego MT-LB ( podwozia obu pojazdów były zbliżone konstrukcyjnie ) .
Pomimo rozpoczęcia produkcji MTLB Ludowe Wojsko Polskie nie posiadało sprecyzowanych planów jego wykorzystania .
Armia Radziecka używała tego pojazdu głównie w roli ciągnika artyleryjskiego do holowania armat przeciwpancernych MT-12 , ale armaty te nie znajdowały się na uzbrojeniu LWP .
W 1978 roku podjęto decyzję o zaprojektowaniu na podwoziu MTLB kilku typów pojazdów .
Wśród nich znalazł się przeciwlotniczy zestaw artyleryjski Promet .
Był uzbrojony w dwie armaty kalibru 23 mm .
Badania prototypu wykazały , że celność prowadzonego przez ten pojazd ognia nie spełnia wymagań .
W rezultacie rozpoczęto projektowanie zmodernizowanego pojazdu oznaczonego jako Promet-2 .
Miał on być uzbrojony w dwie armaty kalibru 30 mm i mieć zaprojektowany przez RADWAR radiolokacyjny system wykrywania i śledzenia celu .
Obliczenia wykazały jednak ze podwozie MTLB ma zbyt małą nośność i w rezultacie w 1980 roku prace nad Prometem-2 przerwano .
