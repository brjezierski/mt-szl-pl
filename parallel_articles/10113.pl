Rużomberk
Rużomberk ( słow .
`` Ružomberok '' , węg .
`` Rózsahegy '' , niem .
`` Rosenberg '' ) – miasto powiatowe w środkowej Słowacji , w kraju żylińskim , w historycznym regionie Liptów .
Rużomberk leży na wysokości 496 m n.p.m .
w Kotlinie Liptowskiej , u ujścia Revúcy do Wagu , między pasmami górskimi Gór Choczańskich na północy , Wielkiej Fatry na południowym zachodzie i Niżnych Tatr na południowym wschodzie .
W 2011 roku liczba mieszkańców miasta wynosiła 28 400 osób , powierzchnia miasta – 126,715 km² .
Składa się z dzielnic Vlkolínec , Biely Potok , Černová , Hrboltová i Rybárpole .
W Rużomberku krzyżują się dwie ważne słowackie drogi krajowe : nr 59 ( R3 / międzynarodowa trasa E77 ) z przejścia granicznego Chyżne – Trzciana do Bańskiej Bystrzycy i nr 18 ( przyszła autostrada D1 / E50 ) z Żyliny do Preszowa .
Przez miasto przebiega linia kolejowa z Popradu do Żyliny .
W mieście rozwinął się przemysł papierniczy , bawełniany , metalowy , poligraficzny oraz materiałów budowlanych .
Historia .
Z wykopalisk wiadomo , że okolice Rużomberka są zamieszkane nieprzerwanie już od epoki brązu .
Sam Rużomberk został założony w XIII wieku przez Słowian , po czym na początku XIV wieku osiedlili się tu koloniści niemieccy ( od nich pochodzi nazwa miasta – niemiecki `` Rosenberg '' stał się słowackim `` Ružomberokiem '' ) .
Prawa miejskie Rużomberk otrzymał w 1318 r. W XIV wieku miasto otrzymywało kolejne przywileje .
Jego wzrost wynikał głównie z położenia miasta na skrzyżowaniu szlaków handlowych , którymi biegną dzisiejsze drogi .
W 1433 r. miasto zostało spalone przez husytów .
W 1729 r. powstało tu gimnazjum pijarskie .
Od swego powstania aż do likwidacji feudalizmu w połowie XIX wieku Rużomberk należał do dominium zamku Likawa ( słow .
`` Likava '' ) .
Miasto było ośrodkiem zróżnicowanego rzemiosła .
W XVIII wieku w Rużomberku powstał pierwszy zakład przemysłowy – papiernia .
Pod koniec XIX wieku zbudowano tu również fabrykę płótna .
