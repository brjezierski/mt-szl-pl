Rwanda
Rwanda ( Republika Rwandy , dawniej Ruanda ) – państwo w środkowo-wschodniej Afryce o powierzchni 26,4 tys .
km² .
Rwanda graniczy z : Burundi ( 290 km ) , Demokratyczną Republiką Konga ( 217 km ) , Tanzanią ( 217 km ) i Ugandą ( 169 km ) .
W 2022 roku w Rwandzie mieszkało około 13,3 mln mieszkańców .
Językami urzędowymi są : rwanda , angielski i francuski .
Stolicą państwa jest Kigali .
Od 2006 roku Rwanda podzielona jest na 5 prowincji ( dawniej było ich 12 ) .
Walutą Rwandy jest frank rwandyjski .
Rwanda należy do : Międzynarodowej Organizacji Pracy , Międzynarodowego Banku Odbudowy i Rozwoju , Międzynarodowego Funduszu Walutowego , Organizacji Narodów Zjednoczonych , Światowej Organizacji Handlu , Unii Afrykańskiej .
W listopadzie 2009 roku Rwanda przystąpiła do Wspólnoty Narodów .
Geografia .
Rwanda znajduje się w strefie Wielkiego Rowu Zachodniego , cała powierzchnia państwa składa się z wyżyn wzniesionych na zachodzie 2500–3000 m , opadających stopniami ku wschodowi .
Na północnym zachodzie znajduje się wulkan Karisimbi ( 4507 m ) .
Klimat jest podrównikowy wilgotny ( odmiana górska ) .
Średnia temperatura miesięczna wynosi : od 17–18 °C w czerwcu , lipcu , do 20–21 °C we wrześniu , październiku .
Na zachodzie średnia roczna suma opadów wynosi 1300–1500 mm ( w grupie górskiej Wirunga 2000 mm ) , a na wschodzie od 900–1000 mm .
Pora deszczowa zaczyna się we wrześniu , październiku i trwa do maja , czerwca .
Rwanda posiada gęstą sieć rzeczną .
Wschodnia część Rwandy należy do dorzecza Nilu , a zachodnia do dorzecza Konga .
Główną rzeką jest Kagera ( źródłowa Nilu ) .
