Kąty Rybackie ( przystanek kolejowy )
Kąty Rybackie – zlikwidowany wąskotorowy przystanek osobowy znajdujący się we wsi Kąty Rybackie w gminie Sztutowo , w powiecie nowodworskim , w województwie pomorskim .
Położony jest na linii kolejowej ze Stegny Gdańskiej do Piasków .
Odcinek tej linii ze Sztutowa do Piasków otwarto w 1944 roku , jednak po 1945 roku został on rozebrany .
