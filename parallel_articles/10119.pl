W latach 1984–1989 nosiło oficjalnie nazwę „ Andropow ” , na cześć sekretarza generalnego KPZR Jurija Andropowa .
W pobliżu miasta znajduje się port lotniczy Starosielje .
Historia .
Pierwsze wzmianki pochodzą z 1071 r. Początkowo był nazywany Ust-Szeksna , w 1504 nadano mu nową nazwę Rybia Wolność , od 1777 wieś uzyskała prawa miejskie i została nazwana Rybińsk .
W 1921–1923 było to centrum Rybińskiej Prowincji .
W 1946 przemianowane na Szczerbakow ( na cześć działacza komunistycznego Aleksandra Szczerbakowa ) , w 1957 została przywrócona historyczna nazwa Rybińsk ; w 1984 przemianowane na Andropow , w 1989 nazwę Rybińsk ponownie przywrócono .
W 2006 przyznano status powiatu miejskiego .
