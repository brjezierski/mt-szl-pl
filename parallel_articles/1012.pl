Hokej na lodzie
Hokej na lodzie ( popularna forma skrócona : `` hokej '' ) – sportowa gra zespołowa rozgrywana na lodowisku .
Hokej na lodzie jest najbardziej popularny w krajach północnej półkuli , co wynika z uwarunkowań klimatycznych .
Jest również popularny na terenach , gdzie sezonowo ( w czasie zimy ) można pokryć dane miejsce lodem , to znaczy w Kanadzie , Stanach Zjednoczonych , Polsce , Rosji , Czechach , Słowacji , Niemczech , Szwajcarii oraz w krajach nordyckich ( Szwecja , Finlandia , Norwegia ) .
Wraz z pojawieniem się lodowisk w zamkniętych halach sport ten można rozgrywać przez cały rok .
Hokej na lodzie jest jednym z czterech najpopularniejszych sportów zawodowych w Ameryce Północnej .
Na terenie Kanady i Stanów Zjednoczonych rozgrywana jest najważniejsza na świecie klubowa liga świata : NHL .
Jest to oficjalny zimowy sport narodowy Kanady , gdzie gra ta cieszy się ogromną popularnością .
Organizacją promującą hokej na lodzie na świecie jest IIHF , czyli `` Międzynarodowa Federacja Hokeja na Lodzie '' .
Skupia ona w sumie 68 krajów z całego świata .
Pomimo tego zdecydowaną większość medali rozgrywanych pod egidą IIHF Mistrzostw Świata zdobyło siedem państw ( tzw .
`` wielka siódemka '' ) : Kanada , Czechy , Finlandia , Rosja/ZSRR , Słowacja , Szwecja i Stany Zjednoczone .
Podczas 22 turniejów olimpijskich wśród mężczyzn z 66 medali , aż 60 zdobyły reprezentacje powyższych państw .
W pięciu rozegranych turniejach kobiet medale zdobyły tylko reprezentacje Kanady , USA , Szwecji , Finlandii oraz Szwajcarii .
Historia .
Początki hokeja sięgają końca XV wieku .
Dyscyplina została prawdopodobnie zapoczątkowana w Anglii lub Francji .
W kulturze po raz pierwszy hokej można zaobserwować w malarstwie niderlandzkim – np .
na obrazie Pietera Bruegla `` Myśliwi na śniegu '' z 1565 .
Na jego dalszym planie widać ludzi korzystających z zakrzywionych kijów .
Jeszcze wcześniej pojawił się na namalowanym w roku 1494 ołtarzu św .
