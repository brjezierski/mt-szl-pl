Rychnov nad Kněžnou ( stacja kolejowa )
Rychnov nad Kněžnou ( czeski : `` Železniční stanice Rychnov nad Kněžnou '' ) – stacja kolejowa w miejscowości Rychnov nad Kněžnou , w kraju hradeckim , w Czechach .
Jest ważnym węzłem kolejowym o znaczeniu regionalnym .
Znajduje się na wysokości 310 m n.p.m .
Jest obsługiwana i zarządzana przez České dráhy .
Na stacji istnieje możliwości zakupu biletów i rezerwacji miejsc .
