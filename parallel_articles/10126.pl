W wyniku połączenia samodzielnych gmin : Rydułtów Górnych , Rydułtów Dolnych , Radoszów Górnych i Radoszów Wielkich 20 października 1926 r. Rydułtowy stały się gminą .
Gmina Pietrzkowice została przyłączona 1 stycznia 1951 r. W 1947 r. przyłączono kolonię Nalas należącą uprzednio do gminy Krzyżkowice .
Zgodnie z rozporządzeniem Rady Ministrów z 8 listopada 1950 r. , Rydułtowy w 1951 r. uzyskały prawa miejskie .
Na mocy ustawy z dnia 28 maja 1975 r. o dwustopniowym podziale administracyjnym państwa oraz zmianie ustawy o radach narodowych , stały się jedną z dzielnic Wodzisławia Śląskiego , ustawa weszła w życie 1 czerwca 1975 r. Z dniem 1 stycznia 1992 r. Rydułtowy odzyskały prawa miejskie .
W latach 1992-1998 miasto administracyjnie należało do województwa katowickiego .
