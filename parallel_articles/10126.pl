Nazwa .
Rudolphi Villa była nazwą wsi księstwa raciborskiego znajdująca się w księgach arcybiskupa wrocławskiego pochodzących z 1228 r. Nazwa Rydułtowy pochodzi od imienia Rudolf , prawdopodobnie należącego do pierwszego właściciela , feudała ; jest to nazwa dzierżawcza .
Cytat z 1300 r. , z dokumentu kodeksu dyplomatycznego Śląska właśnie takie pochodzenie nazwy sugeruje : „ In villa Rudolphi sunt XXX mansi solventes fertones ” – „ we wsi Rudolfa 30 łanów uiszczających ćwierć grzywny ” .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295-1305 miejscowość wymieniona jest w zlatynizowanej formie `` Rudolphi villa '' .
Kronika ta wymienia również wsie , które w procesach urbanizacyjnych zostały wchłonięte przez miasto i stanowią jego części bądź dzielnice jak założona na prawie polskim łac .
