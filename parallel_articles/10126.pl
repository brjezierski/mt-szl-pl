Rydułtowy
Rydułtowy ( ) – miasto w południowej Polsce , w województwie śląskim , w powiecie wodzisławskim .
Według danych pochodzących z 31 grudnia 2019 r. miasto miało 21 514 mieszkańców .
Położenie .
Rydułtowy są położone w południowo-zachodniej części Wyżyny Śląskiej na Płaskowyżu Rybnickim , który na południu graniczy z Kotliną Oświęcimsko-Raciborską .
W rejonie wieży ciśnień , na granicy administracyjnej miasta leży najwyższy punkt Płaskowyżu Rybnickiego , jego wysokość wynosi 311 m n.p.m .
Położenie geograficzne 18°26′E 50°04′N .
Granice Rydułtów wytyczają miasta : Rybnik , Radlin , Pszów , oraz gminy : Kornowac i Gaszowice .
Miasto jest położone w strefie nadgranicznej z Czechami .
Rydułtowy znajdują się w powiecie wodzisławskim i należą do aglomeracji rybnickiej .
Historycznie Rydułtowy położone są na Górnym Śląsku .
Środowisko naturalne .
Budowa geologiczna .
Budowa geologiczna obszaru na którym znajduje się miasto składa się z warstw karbońskich z licznymi pokładami węgla kamiennego , oraz z osadów czwartorzędowych .
Wychodnia piaskowców karbońskich , należących do warstw porębskich znajduje w pobliżu ulicy Skalnej .
Wychodnia jest objęta ochroną prawną uchwałą Rady Miasta Rydułtowy z dnia 26 kwietnia 1994 r. , nadano jej nazwę Stanowisko Przyrody Nieożywionej Skałka .
Utwory czwartorzędowe , to piaski , gliny , żwiry , mady rzeczne i namuły .
W północno-wschodniej części miasta znajdują się złoża piaszczysto-żwirowe , w południowo-wschodniej eksploatowane są złoża gliny .
Rzeźba terenu Rydułtów jest wynikiem działania lodowca i wód płynących .
Warunki klimatyczne .
Miasto znajduje się w zasięgu dwóch regionów klimatycznych Śląsko-Wielkopolskiego i Nizinnego-Podkarpackiego .
Występujące wiatry głównie pochodzą z kierunków południowo-zachodnich i północno-zachodnich .
Klimat należy do umiarkowanych .
Najchłodniejszym miesiącem zimy jest styczeń , w którym przeciętna temperatury wynosi −2 stopnie Celsjusza .
Lipiec jest najcieplejszym miesiącem w roku ze średnią temperaturą +17 stopni Celsjusza .
Temperatura poniżej zera przypada 90 dni w roku , a powyżej 15 stopni 100 dni .
Średnie opady w styczniu wynoszą 50 mm , a w lipcu 100 mm , zaś średni roczny opad wynosi ok. 750 mm-800 mm .
Śnieg pokrywa Rydułtowy 55-60 dni w roku .
