Historia .
W połowie XIX w. większość mieszkańców miejscowości oraz regionu stanowiła ludność polskojęzyczna .
Topograficzny opis Górnego Śląska z 1865 r. odnotowuje , że `` „ We wsi znajduje się 234 gospodarstw domowych z 1066 mieszkańców mówiących po polsku ” '' – `` „ Es befinden sich im Dorfe 234 Haushaltungen mit 1066 polnisch sprechenden Einwohnern ” '' .
