Rye ( rzeka w Anglii )
Rye ( ang .
`` River Rye '' ) – rzeka w północno-wschodniej Anglii , w hrabstwie North Yorkshire , dopływ rzeki Derwent .
Źródło rzeki znajduje się na wzgórzu Whorlton Moor , w zachodniej części parku narodowego North York Moors , na wysokości około 305 m n.p.m .
Rzeka płynie w kierunku południowo-wschodnim .
Wzdłuż rzeki położone są miejscowości Hawnby , Rievaulx , Helmsley , Nunnington , Butterwick , Brawby , Little Habton , Great Habton , Ryton i Wykeham .
Za tą ostatnią , na północny wschód od miasta Malton rzeka uchodzi do Derwent , na wysokości około 15 m n.p.m .
