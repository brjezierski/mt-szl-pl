( 1796 ) Riga
( 1796 ) Riga – planetoida należąca do zewnętrznej części pasa głównego planetoid okrążająca Słońce w ciągu 6 lat i 48 dni w średniej odległości 3,35 au .
Została odkryta 16 maja 1966 roku w Krymskim Obserwatorium Astrofizycznym w Naucznyj na Półwyspie Krymskim przez Nikołaja Czernycha .
Nazwa planetoidy pochodzi od Rygi , stolicy Łotwy .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 1796 ) 1966 KB .
