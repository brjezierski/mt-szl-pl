Ryga ( przyrząd )
Ryga , inaczej liniuszek – pomoc szkolna w postaci arkusza sztywnego papieru formatu A4 lub A5 , obecnie rzadko używana .
Ryga zadrukowana jest z jednej strony liniami oddalonymi od siebie o 1 cm , a z drugiej kratką o odstępie pomiędzy liniami 5 mm .
Przyrząd stosuje się w przypadku pisania w zeszytach zawierających strony niezadrukowane ani liniami , ani kratką ( tzw .
czystych , gładkich ) , podkładając go pod czystą kartkę , aby można było na niej pisać równo .
