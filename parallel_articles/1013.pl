Lodownia
Lodownia , lodnia – pomieszczenie lub osobny budynek służące do przechowywania wyłącznie lodu lub śniegu .
Lodownie były lokalizowane blisko chłodni , w których przechowano produkty łatwo psujące się bez udziału agregatów chłodniczych , albo były jednocześnie chłodnią .
W lodowniach i chłodniach uzyskiwano stałą temperaturę 4°C .
Historia .
Przez długi czas lodownie ze względu na koszty budowy i utrzymania były dobrem luksusowym , na który mogły sobie pozwolić bogate rodziny , wyższe sfery .
Upowszechnienie dużych lodowni spowodowało wzrost zapotrzebowania na lód naturalny .
Wielka Brytania pod koniec XIX wieku importowała rocznie ok. 500 000 ton lodu naturalnego .
Natomiast jego roczne zużycie w Stanach Zjednoczonych ok. 1890 roku wynosiło 25 milionów ton .
W 1862 r. skonstruowano pierwszą chłodziarkę mechaniczną .
Wraz z rozwojem chłodnictwa mechanicznego zmniejszała się konieczność gromadzenia i przechowywania lodu naturalnego , ponieważ zaczęto produkować go na bieżąco .
Pierwsza fabryka sztucznego lodu powstała w Cleveland ( USA ) .
Kolejne powstawały w Anglii ( 1857 r. ) , Francji ( 1858 r. ) i Japonii ( 1872 r. ) .
Upowszechnienie się chłodziarek mechanicznych ostatecznie zdecydowało o zaniku zainteresowania lodowniami i chłodniami lodowymi
Budowa .
Lodownie stanowiły osobny budynek lub były zakładane w murowanych piwnicach w zagłębieniach terenu , w miejscach zaciemnionych , blisko zabudowań ludzkich .
W terenie podmokłym były budowane na powierzchni lub lekkim zagłębieniu .
Wnętrze często z półkolistym sklepieniem było wzmocnione kamieniem , z muru wystawały haki , na których wieszano mięso .
Lodownia była wyposażona w kanał odprowadzający wodę z topniejącego lodu .
Do lodowni prowadził przeważnie długi korytarz z parą drzwi .
Lód .
Chłodzenie produktów w chłodni odbywało się za pomocą lodu naturalnego .
Lód był pozyskiwany zimą ze stawów , jezior i rzek w postaci tafli i brył .
Jeśli nie było doń dostępu , ubijano śnieg i polewano go wodą , by po zamrożeniu stworzył bryłę lodu .
Lód do lodowni był zwożony saniami i ustawiany na drewnianych półkach , a następnie izolowany przez przykrycie słomą , trocinami , mchem lub sitowiem .
