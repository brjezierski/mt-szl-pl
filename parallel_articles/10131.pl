Ryglice ( gromada )
Ryglice – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Ryglice z siedzibą GRN w Ryglicach ( wówczas wsi ) utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie tarnowskim w woj .
krakowskim , na mocy uchwały nr 30/IV/54 WRN w Krakowie z dnia 6 października 1954 .
W skład jednostki wszedł obszar dotychczasowej gromady Ryglice ze zniesionej gminy Ryglice w tymże powiecie .
Dla gromady ustalono 25 członków gromadzkiej rady narodowej .
1 stycznia 1958 do gromady Ryglice przyłączono obszar zniesionej gromady Bistuszowa-Uniszowa .
31 grudnia 1961 do gromady Ryglice przyłączono obszar zniesionej gromady Joniny .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 reaktywowano gminę Ryglice .
