Rykacze
Rykacze – wieś w Polsce położona w województwie podlaskim , w powiecie zambrowskim , w gminie Zambrów .
Historia .
W latach 1921–1939 wieś leżała w województwie białostockim , w powiecie łomżyńskim , w gminie Długobórz .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 138 osób w 20 budynkach mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w Zambrowie .
