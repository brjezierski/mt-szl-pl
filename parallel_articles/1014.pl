Ajuricaba ( 1945 )
Ajuricaba ( A-3 ) – brazylijski niszczyciel z okresu „ zimnej wojny ” , jedna z sześciu jednostek typu `` Acre '' .
Okręt został zwodowany 30 maja 1945 roku w stoczni Arsenal de Marinha do Rio de Janeiro na Ilha das Cobras , a do służby w Marinha do Brasil wszedł w grudniu 1957 roku .
W 1962 roku niszczyciel nieopodal Rio de Janeiro wszedł na podwodną skałę i doznał poważnych uszkodzeń .
Jednostka została skreślona z listy floty w lipcu 1964 roku .
Projekt i budowa .
