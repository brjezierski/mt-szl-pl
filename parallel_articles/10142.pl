Lactarius pergamenus
Lactarius pergamenus ( Sw. ) Fr . )
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1809 r. Olof Peter Swartz nadając mu nazwę `` Agaricus pergamenus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1838 r. Elias Fries , przenosząc go do rodzaju `` Lactarius '' .
Synonimy łacińskie ) :
W niektórych atlasach grzybów jest podawana jego nazwa polska – mleczaj skórzasaty .
Morfologia .
Średnicy 5–12 ( do 15 ) cm , u młodych owocników biały , w trakcie dojrzewania pojawiają się na nim ochrowe plamy .
Pokryty suchą , matową skórką .
