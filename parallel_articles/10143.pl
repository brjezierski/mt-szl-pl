W latach 1975–1998 miasto administracyjnie należało do województwa włocławskiego .
Historia .
Rypin jest jedną z najstarszych polskich miejscowości .
Pierwsze informacje o nim pochodzą z tzw .
Przywileju Bolesława Śmiałego dla klasztoru Benedyktynów w Mogilnie z dnia 11 kwietnia 1065 roku .
Już w XI w. gród stał się stolicą odrębnej jednostki terytorialnej , zwanej kasztelanią , a od XIV w. – powiatu .
W ciągu tysiąclecia swych dziejów Rypin czterokrotnie pozbawiany był swej pozycji miasta stołecznego północnej części ziemi dobrzyńskiej .
