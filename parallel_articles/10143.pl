Rypin
Rypin ( niem .
`` Rippin '' ) – miasto w Polsce położone we wschodniej części województwa kujawsko-pomorskiego , siedziba władz powiatu i gminy .
Rypin był miastem królewskim Korony Królestwa Polskiego .
Położone na szlaku wiodącym z Kujaw na Warmię i z Pomorza na Mazowsze .
Leży w północno-wschodniej części historycznej ziemi dobrzyńskiej nad rzeką Rypienicą – dopływem Drwęcy .
Położenie .
Geograficznie obszar otaczający Rypin należy do Pojezierza Rypińskiego .
Ze względu na duże skupiska jezior znajdujących się na terenie gmin powiatu rypińskiego .
