Przepuklina
Przepuklina ( ) – patologiczne uwypuklenie zawartości jamy ciała ( najczęściej jamy brzusznej ) przez otwór w powłokach , w miejscu zwanym miejscem o obniżonej oporności ( ) .
Budowa przepukliny .
Przepuklina posiada wrota przepukliny , kanał przepukliny , worek przepuklinowy oraz zawartość .
Etiologia i występowanie .
Przepukliny dzielimy na :
Duży odsetek przepuklin stanowią przepukliny pooperacyjne będące powikłaniem wielu zabiegów jamie brzusznej .
Ocenia się , że przepukliny takie powstają w 2-10 % laparotomii .
Na ich powstanie ma wpływ zarówno błąd chirurga ( niewłaściwa technika czy szwy ) , jak i zaburzenia gojenia rany wynikające ze stanu chorego lub wrodzonych zaburzeń syntezy i degradacji kolagenu .
Przepukliny pooperacyjne powstają znamiennie częściej po operacjach w trybie pilnym niż po zabiegach planowych .
Najczęstszymi przepuklinami pooperacyjnymi są przepukliny kresy białej ( cięcie pośrodkowe ) lub okołopępkowe .
Te drugie są najczęściej skutkiem nieprawidłowego zarośnięcia rany po operacji laparoskopowej .
Najczęściej występujące przepukliny brzuszne to : pachwinowa ( 70 % ) , udowa ( 12 % ) , pępkowa ( 11 % ) .
