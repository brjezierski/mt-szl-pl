Ryszard Kotys
Ryszard Czesław Kotys ( ur .
20 marca 1932 w Mniowie jako Ryszard Czesław Kotas , zm .
27 stycznia 2021 w Poznaniu ) – polski aktor filmowy , telewizyjny , teatralny i dubbingowy , reżyser i scenarzysta .
Życiorys .
Był synem Czesława Kotasa ( 1903–1984 ) i Ireny ( ok. 1912–1984 ) .
W 1953 roku ukończył Państwową Wyższą Szkołę Aktorską w Krakowie , debiutował na deskach Teatru im .
Stefana Żeromskiego w Kielcach .
Przez następne lata występował kolejno w teatrach : Wybrzeże w Gdańsku , Polskim we Wrocławiu , Ludowym w Krakowie , Ziemi Krakowskiej w Tarnowie , Dramatycznym im .
Aleksandra Węgierki w Białymstoku , im .
Jana Kochanowskiego w Opolu i im .
Stefana Jaracza w Łodzi , gdzie zajmował się również reżyserią .
W ciągu ponad 65 lat kariery zagrał w ponad 40 serialach i 150 filmach fabularnych .
Popularność przyniosła mu rola Mariana Paździocha , sąsiada Ferdynanda Kiepskiego , którą odgrywał przez 21 lat w serialu telewizyjnym `` Świat według Kiepskich '' .
Był także jednym z kandydatów do roli głównego bohatera .
