Ryszkowa Wola
Ryszkowa Wola – wieś w Polsce położona w województwie podkarpackim , w powiecie jarosławskim , w gminie Wiązownica .
Miejscowość jest siedzibą rzymskokatolickiej parafii Świętych Apostołów Piotra .
Prywatna wieś szlachecka , położona w województwie ruskim , w 1739 roku należała wraz z folwarkiem do klucza Jarosław Lubomirskich .
4 maja 1945 grupa Polaków zabiła 5 osób mieszkających na plebanii , między innymi matkę biskupa Andrija Sapeliaka .
W latach 1975–1998 miejscowość administracyjnie należała do województwa przemyskiego .
