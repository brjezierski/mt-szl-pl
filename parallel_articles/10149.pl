Rytele-Wszołki
Rytele-Wszołki – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Ceranów .
Leży na lewym brzegu Bugu .
Zaścianek szlachecki Wszołki należący do okolicy zaściankowej Rytele położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W miejscowości znajduje się kaplica dojazdowa .
Wierni wyznania Rzymskokatolickiego zamieszkali we wsi należą do parafii Kosów Lacki .
