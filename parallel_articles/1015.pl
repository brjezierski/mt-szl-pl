Leśnicy usuwają złomy z lasu , ponieważ pogarszają one jego tak zwany stan sanitarny .
Pochodzące z nich drewno przeznaczone jest często na sortyment niższej jakości .
Złomy spełniają jednak istotną rolę w ekosystemie leśnym – są na przykład siedliskiem życia wielu ważnych gatunków organizmów leśnych ( grzybów , roślin naczyniowych , owadów , ptaków , drobnych ssaków i in . )
i ich eliminacja może stanowić dla nich zagrożenie .
Zakres stosowania pojęcia złom .
W części źródeł daje się zauważyć poważną niekonsekwencję w stosowaniu pojęcia złom .
Według ustawy o ochronie przyrody złom to drzewo , którego pień uległ złamaniu , lub krzew , którego pędy uległy złamaniu w wyniku działania czynników naturalnych , wypadku lub katastrofy w ruchu lądowym , wodnym lub powietrznym , lub katastrofy budowlanej .
Złomem może tutaj być więc również krzew .
Z kolei Wydział Ochrony Środowiska i Rolnictwa Urzędu Miasta Łodzi w swoich wytycznych z 2015 roku dotyczących postępowania ze zniszczonymi drzewami precyzuje , że złom to drzewo złamane na odcinku od szyi korzeniowej do korony na wysokości powyżej 0,5 m. Odłamana górna część z koroną o ile wciąż połączona jest z dolną określana jest nazwą złom całkowity .
Według „ `` Małej encyklopedii leśnej '' ” z 1980 roku jest to złamane drzewo lub część drzewa ( konary ) złamana na pniu , jak również wymienione wyżej złomy zrębowe .
Natomiast według bardziej lakonicznej definicji w jej nowszym wydaniu z 1991 roku jest to złamane drzewo .
Z kolei „ `` Słownik języka polskiego '' ” PWN z 1981 roku stosuje ten termin w zupełnie innym znaczeniu – jako gałąź , łodygę odłamaną na skutek silnego wiatru , gradu , sadzi ( szadzi ) itp .
( złomy topoli , świerka , sosny ) , natomiast jego najnowsze wydanie internetowe z kolei w szerszym zakresie jako połamane drzewo lub odłamaną gałąź .
Jednak przeznaczony do praktycznego stosowania „ `` Niezbędnik leśnika '' ” z 2012 roku wyraźnie zaznacza , że do złomów ( złamanych drzew stojących ) nie zalicza się ( nie dokłada się ) ich odłamanych fragmentów .
