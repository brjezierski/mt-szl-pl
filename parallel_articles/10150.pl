Wólka Rytelska
Wólka Rytelska – wieś w Polsce , położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Ceranów .
Obok wsi przepływa rzeczka Buczynka , dopływ Bugu .
W latach 1975–1998 wieś administracyjnie należała do województwa siedleckiego .
Mieszkańcy wyznania rzymskokatolickiego należą do parafii Niepokalanego Poczęcia NMP w Ceranowie .
