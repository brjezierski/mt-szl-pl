Reńska Wieś ( stacja kolejowa )
Reńska Wieś – dawna stacja kolejowa położona we wsi Reńska Wieś .
Historia .
Stacja Reńska Wieś przed 1945 nosiła nazwę `` Reinschdorf '' .
Od 1989 do 1997 istniała tutaj stacja pasażerska .
Po 1997 roku fragment linii przechodzący przez stacją został zamknięty dla ruchu pasażerskiego i towarowego , a następnie zlikwidowany .
