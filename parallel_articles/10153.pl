Rzeczniówek
Rzeczniówek – wieś w Polsce położona w województwie mazowieckim , w powiecie lipskim , w gminie Rzeczniów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa radomskiego .
We wsi urodził się ekonomista Konrad Bajan .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Restytuta i Niepokalanego Poczęcia Najświętszej Maryi Panny w Rzeczniowie .
