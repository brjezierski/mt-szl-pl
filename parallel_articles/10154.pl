Rzepcze
Rzepcze ( dodatkowa nazwa w j. niem .
Repsch ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Kotliny Raciborskiej , będącej częścią Niziny Śląskiej .
Przepływa przez nią rzeka Osobłoga .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 299 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 13 km od granicy z Czechami , w zachodniej części Kotliny Raciborskiej , tuż przy granicy powiatu prudnickiego z powiatem krapkowickim ( gmina Krapkowice ) .
Należy do Euroregionu Pradziad .
Przez granice administracyjne wsi przepływa rzeka Osobłoga .
Środowisko naturalne .
W Rzepczu panuje klimat umiarkowany ciepły .
