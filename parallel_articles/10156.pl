Rzepnica ( dodatkowa nazwa w j. kaszub .
Rzepnica ; niem .
`` Sepnitz '' ) – wieś kaszubska w Polsce na Pojezierzu Bytowskim położona w województwie pomorskim , w powiecie bytowskim , w gminie Bytów przy 228 .
Na terenie wsi znajduje się jezioro Rzepnica oraz wiele lasów .
Wieś bezpośrednio przylega do Bytowa i stanowi już właściwie jego północno-wschodnie przedmieście .
Osada powstała w końcu XVI wieku jako owczarnia w dobrach książęcych Mądrzechowa .
Z czasem rozrosła się do niewielkiej wsi .
Na początku XX wieku przez Rzepnicę poprowadzono linię kolejową z Bytowa do Lęborka , co przyczyniło się do rozwoju osady .
Z racji bliskości Bytowa w latach .70 i .80 Rzepnica stała się szybko rozwijającym się osiedlem podmiejskim .
W przeważającej części Rzepnica stanowi osiedle domków jednorodzinnych .
