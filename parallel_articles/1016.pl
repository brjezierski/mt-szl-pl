Akaa
Akaa ( szw .
`` Ackas '' ) – miasto i gmina w Finlandii , w regionie Pirkanmaa .
Powstało 1 stycznia 2007 roku , kiedy miasto Toijala i gmina Viiala zostały połączone w jedno miasto .
1 stycznia 2011 przyłączono do Akaa gminę Kylmäkoski .
Populacja wynosi 17 028 ( 2014 ) .
Powierzchnia wynosi 114,67 km² , z czego 12,5 km² to woda .
Miasto znajduje się nad jeziorami Vanajavesi oraz Nahkiala .
W Toijali oraz Viiali znajdują się stacje kolejowe .
