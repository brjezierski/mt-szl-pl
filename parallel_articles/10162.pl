Rzymkowice
Rzymkowice ( ) – wieś w Polsce , położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Równiny Niemodlińskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 591 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 18 km od granicy z Czechami , w południowej części Równiny Niemodlińskiej , tuż przy granicy gminy Korfantów z gminą Biała ( powiat prudnicki ) .
Należy do Euroregionu Pradziad .
Środowisko naturalne .
W Rzymkowicach panuje klimat umiarkowany ciepły .
Średnia temperatura roczna wynosi +8,3 °C .
Duże zróżnicowanie dotyczy termicznych pór roku .
Średnie roczne opady atmosferyczne w rejonie Rzymkowic wynoszą 605 mm .
