Krzywa stanów wody obrazuje wahania stanów wód w zależności od czasu w określonym profilu .
Okresowe stany wód ( rok , półrocze , okres żeglugi ) .
Stan wody w rzece informuje o napełnieniu koryta w danym profilu , lecz nie informuje o natężeniu przepływu , a tym samym o dopływie rzecznym .
Napełnienie koryta rzeki zależy od natężenia przepływu .
Naturalną regulacja rzek nazywa się regulację , której cel osiąga się przez mało różniące się od naturalnego ukształtowania koryta i tarasów zalewowych .
W celu stworzenia nowej „ naturalnej ” rzeki o parametrach dostosowanych do potrzeb gospodarczych ( rolnictwo , ujęcia wody , dopływy , mosty , ochrona brzegów , rekreacja ) .
Rzekę przystosowaną do żeglugi za pomocą stopni wodnych nazywa się rzeką skanalizowaną .
