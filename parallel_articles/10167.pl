Z biegiem rzeki zmienia się charakterystyka koryta rzecznego , prędkość prądu .
Zmienia się również struktura biocenozy .
Powstaje poziomy podział cieku wzdłuż jego biegu .
Budowa rzeki .
Początek rzeki może mieć formę źródła lub obszaru źródliskowego , choć nierzadko zdarza się , że rzeka powstaje z połączenia kilku mniejszych potoków lub wypływa z jeziora , lodowca lub bagna .
Zasilana jest wodami z opadów atmosferycznych , a także przez wody podziemne .
Rzeka stała uchodzi do innej rzeki , jeziora , morza lub wysycha przed ujściem do morza .
Bardzo wyjątkową , godną odnotowania rzeką jest Onyx – morze jest jej źródłem , a nie celem .
Rzeki uchodzące do zbiornika wodnego nazywa się rzekami głównymi , pozostałe to ich dopływy .
Rzeka główna wraz z dopływami tworzy system rzeczny .
Bieg rzeki dzieli się zazwyczaj na trzy odcinki : górny , środkowy i dolny , które wyróżnia odmienny udział erozji i akumulacji .
Ujście rzeki może mieć charakter lejkowaty ( estuarium ) bądź delty .
Reżim rzeki ( ustrój rzeki ) określa przebieg zjawisk hydrologicznych na rzece : zmiany stanów wody ( wezbrania , niżówki ) , zjawiska lodowe itp .
Płynąca woda w rzece powoduje ciągłe jej mieszanie i napowietrzanie .
W rzece nie występują uwarstwienia wody , które charakterystyczne są dla jezior .
Obrzeża rzek i starorzeczy zasiedlają wielogatunkowe zbiorowiska roślin wodnych .
Pojęcia charakterystyczne dla rzeki .
Badaniem rzek zajmuje się dział hydrologii – potamologia .
Badanie jakości wód w rzekach odbywa się w układzie trzystopniowym i obejmuje : sieć krajową , sieci regionalne oraz sieci lokalne .
Sieć krajową rzek obejmuje : sieć reperową , sieć podstawową oraz sieć graniczną .
Z siecią rzek związane są zbiorniki zaporowe zabezpieczające przed powodzią , ułatwiające żeglugę , dostarczające energię elektryczną oraz zabezpieczające ludność w wodę pitną .
