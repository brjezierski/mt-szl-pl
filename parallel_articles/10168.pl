Modlitwa
Modlitwa – czynność kultowa , występująca w wielu religiach .
Polega na skierowaniu swoich myśli do istoty lub istot , mogących być lub będących przedmiotem kultu ( bogowie , święci , aniołowie ) .
Charakterystyka modlitwy .
Modlitwa jest czynnością kultową , charakterystyczną dla wielu religii , polegającą na wezwaniu do bóstwa .
Wywodzi się z praktyk magicznych i początkowo miała formę zaklęcia , które według wierzeń posiadało moc oddziaływania na rzeczywistość za pomocą słów .
Za skuteczny element zaklęcia-modlitwy uchodziło wymawianie imienia wzywanego bóstwa .
Wraz z rozwojem religii i kultu religijnego powstały inne rodzaje modlitwy ( np .
dziękczynne ) .
Współcześnie najczęściej modlitwa łączy w sobie różne cechy wezwania : prośbę , podziękowanie , hołd itd .
Często język modlitwy jest sformalizowany i zrozumiały jedynie dla znających język sakralny .
Niekiedy modlitwie towarzyszą określone gesty ( np .
klękanie ) , a podczas jej odmawiania używa się przeznaczonych do tego akcesoriów ( np .
różaniec ) .
W religiach plemiennych towarzyszy jej często taniec rytualny .
W niektórych religiach siła modlitwy zależy od liczby powtarzalnych sformułowań ( tzw .
modlitwa mechaniczna , np .
młynki modlitewne w lamaizmie ) .
Modlitwa w poszczególnych religiach .
Akty modlitewne są powszechne we wszystkich religiach .
Choć modlitwa może przyjmować formę indywidualnej rozmowy z istotą boską , to w wielu religiach ukształtował się pewien kanon modlitw , których treść jest ściśle określona i przekazywana w takiej formie wyznawcom .
Chrześcijaństwo .
W chrześcijaństwie modlitwa jest rozmową z Bogiem osobowym , zwróceniem się do niego w prośbie , dziękczynieniu czy uwielbieniu .
Chrześcijanie uczą się modlitwy od proroków , świętych , a przede wszystkim od Jezusa , który według Ewangelii za życia wiele razy zwracał się do Boga i przekazywał słowa modlitwy swoim uczniom .
Modlitwa jest rozmową duchową , w której większość chrześcijan zwraca się również ( np .
w kościele rzymskokatolickim ) do świętych o wstawiennictwo u Boga .
Trzy główne formy modlitwy w życiu chrześcijanina : modlitwa ustna , rozmyślanie i kontemplacja .
