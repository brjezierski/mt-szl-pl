Akagi ( 1888 )
– kanonierka japońskiej marynarki wojennej , czwarty i ostatni okręt typu `` Maya '' .
Wziął udział w wojnie chińsko-japońskiej , w trakcie której został uszkodzony , i w wojnie rosyjsko-japońskiej .
Budowane w stoczniach japońskich kanonierki typu `` Maya '' były niewielkimi okrętami o mieszanej , żelaznej lub żelazno-stalowej konstrukcji .
„ Akagi ” od swoich poprzedników odróżniał się w pełni stalowym kadłubem i podniesionym forkasztelem .
Okręt posiadał także sponsony burtowe dla broni maszynowej , a w przededniu wojny chińsko-japońskiej na fokmaszcie zamontowano mu mars bojowy , na grotmaszcie zaś – bocianie gniazdo .
Dla dalszego odróżnienia od innych jednostek swego typu „ Akagi ” miał namalowany na burcie czarny pas .
Okręty napędzały dwie maszyny parowe podwójnego rozprężania o mocy 960 ihp ( inne źródło : 700 hp ) ; parę dostarczały dwa cylindryczne kotły a 2 śruby nadawały prędkość 12 węzłów .
Zapas węgla wynosił 60 ton ; kanonierka miała też pełne ożaglowanie typu szkuner .
W 1894 „ Akagi ” uzbrojony był w cztery armaty 120 mm i sześć działek Hotchkissa kal .
