Rzążew
Rzążew – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Zbuczyn .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Od 1937 roku działa we wsi Ochotnicza Straż Pożarna posiadająca własną remizę .
Jednostka jest w posiadaniu dwóch samochodów ratowniczo-gaśniczych średniego GBA 2,5/16 Star 244 i ciężkiego GCBA 6/32 Jelcz 315 .
Na terenie wsi znajdują się : sklep spożywczy , przystanek PKS , kaplica katolicka , należąca do parafii św .
Stanisława i Aniołów Stróżów w Zbuczynie ( wybudowana wspólnymi siłami przez mieszkańców Rzążewa w 1991 roku ) oraz niewielki staw przy remizie OSP .
To typowa wieś placowa .
Przez wieś przepływa ciek zwany Rzężawką .
Od nazwy wsi ( gniazda ) swe nazwisko wywodzi ród Rzążewskich .
Z tej wsi pochodzą m.in .
: poseł na Sejm RP Krzysztof Tchórzewski , ksiądz Wincenty Płudowski , ksiądz Artur Pióro .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Stanisława i Aniołów Stróżów w Zbuczynie .
Historia .
Pierwsza wzmianka o wsi pochodzi z 1531 roku .
Miejscowość za czasów króla Zygmunta I Starego jako Rząssów miała 2 łany .
W XVI wieku znana była pod nazwami Rzazow i Rzarzew – jako wieś szlachecka miała wtedy 55 domów .
W roku 1552 posiadała 2 i 3/4 łana .
W 1827 roku miała 33 domy .
