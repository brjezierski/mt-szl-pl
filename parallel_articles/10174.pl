Râșnov
Râșnov ( , ) – miasto w okręgu Braszów w Siedmiogrodzie w Rumunii .
Jest zamieszkane przez 15 022 mieszkańców ( 2011 ) .
Osada została założona około roku 1215 przez Zakon krzyżacki , pierwsze historyczne wzmianki o mieście pochodzą z roku 1331 .
Miejscowy zamek został zdobyty tylko raz przez Gabriela Batorego w roku 1612 .
W mieście znajduje się kompleks skoczni narciarskich Trambulina Valea Cărbunării .
