( 7098 ) Réaumur
( 7098 ) Réaumur – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 108 dni w średniej odległości 2,64 j.a .
Została odkryta 9 października 1993 roku w Europejskim Obserwatorium Południowym przez Erica Elsta .
Nazwa planetoidy pochodzi od René Antoine Ferchaulta de Réaumura ( 1683–1757 ) , francuskiego naukowca , twórcy skali Réaumura .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 7098 ) 1993 TK39 .
