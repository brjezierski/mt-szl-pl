Régis Genaux
Régis Hervé Genaux ( ur .
31 sierpnia 1973 w Charleroi , zm .
8 listopada 2008 w Chaudfontaine ) – belgijski piłkarz grający na pozycji prawego obrońcy .
22 razy wystąpił w reprezentacji Belgii .
Kariera klubowa .
Karierę piłkarską Genaux rozpoczynał w klubie Royal Charleroi .
W 1988 roku podjął treningi w Standardzie Liège .
W 1990 roku awansował do kadry pierwszej drużyny i wtedy też zadebiutował w jej barwach w pierwszej lidze belgijskiej .
W sezonie 1992/1993 osiągnął swoje pierwsze sukcesy w karierze , gdy wywalczył ze Standardem wicemistrzostwo Belgii oraz zdobył Puchar Belgii .
Z kolei w 1995 roku ponownie został wicemistrzem kraju .
W zespole Standardu występował do końca sezonu 1995/1996 .
