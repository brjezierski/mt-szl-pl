Rödental
Rödental – miasto w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Frankonia , w regionie Oberfranken-West , w powiecie Coburg .
Leży ok. 7 km na północny wschód od Coburga , nad rzeką Röthen , przy autostradzie A73 , drodze B4 i linii kolejowej Coburg – Sonneberg .
