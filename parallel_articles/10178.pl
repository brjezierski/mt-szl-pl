Røros ( stacja kolejowa )
Røros – stacja kolejowa w Røros , w regionie Sør-Trøndelag , w Norwegii .
Stacja znajduje się 399,5 km od Oslo S i około 628 metrów nad poziomem morza .
Stacja obsługuje połączenia regionalne do Trondheim i Hamar .
Stacja została otwarta w 1877 podczas otwarcia linii Rørosbanen .
