Rüsselsheim am Main
Rüsselsheim am Main ( do 29 lipca 2015 `` Rüsselsheim '' ) – miasto w Niemczech , w kraju związkowym Hesja , w rejencji Darmstadt , w powiecie Groß-Gerau .
Rüsselsheim am Main to miasto o szczególnym statusie `` ( Sonderstatusstadt ) '' , oznaczającym , że przejęło niektóre zadania powiatu .
Siedziba firmy motoryzacyjnej Opel .
W mieście znajduje się stacja kolejowa Rüsselsheim .
