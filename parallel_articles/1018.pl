Akaroa
Akaroa – miejscowość w Nowej Zelandii , w regionie Canterbury , na Wyspie Południowej .
W 2013 roku liczyła 642 mieszkańców .
Znajduje się na Półwyspie Banksa .
Została zasiedlona przez Europejczyków w 1840 roku .
Leży u stóp wulkanu , nad morzem .
W obrębie portu można spotkać toniny nowozelandzkie ( gatunek delfina ) .
