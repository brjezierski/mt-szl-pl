Rąb
Rąb ( dodatkowa nazwa w j. kaszub .
Rąb ) – wieś kaszubska w Polsce , położona w województwie pomorskim , w powiecie kartuskim , w gminie Przodkowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Starą szkołę na Rębie wybudowano i założono w 1921 roku , zamknięto w 2001 , obecnie mieści się tam prywatne muzeum kaszubskie prowadzone przez Zofię Gusman , córkę B. Gusman , potomków nauczycieli i dyrektora szkoły .
W latach dziewięćdziesiątych XX wieku odkryto tutaj cmentarzysko na polu należącym do Zbigniewa Pranczke , składające się z 52 urn z prochami ludzkimi sprzed 2,5 tysiąca lat .
