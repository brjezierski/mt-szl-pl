Jakuba .
Rąbiń oraz okoliczne wsie ( takie jak : Turew , Błociszewo , czy Stary Gołębin ) , leżące na terenie czterech gmin , w dwóch różnych powiatach – kościańskim i śremskim , stanowią z rolniczo-kulturowego punktu widzenia cenny obszar .
Właśnie dlatego wraz z Rąbiniem , również kilkanaście innych miejscowości objętych zostało ochroną przez Park Krajobrazowy im .
gen. Dezyderego Chłapowskiego , który utworzono w 1992 roku .
Historia .
W okresie Wielkiego Księstwa Poznańskiego ( 1815–1848 ) miejscowość wzmiankowana jako Rombin należała do wsi większych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Rombin należał do okręgu krzywińskiego tego powiatu i stanowił część majątku Turwia ( dziś Turew ) , którego właścicielem był wówczas ( 1846 ) gen. Dezydery Chłapowski .
W skład majątku Turwia wchodził także folwark Rombinek oraz Wronowo .
Według spisu urzędowego z 1837 roku Rombin liczył 440 mieszkańców , którzy zamieszkiwali 52 dymy ( domostwa ) .
W okresie 1975-1998 miejscowość znajdowała się w leszczyńskiem .
