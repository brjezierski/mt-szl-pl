Rumia
Rumia ( kaszb .
`` Rëmiô '' , niem .
`` Rahmel '' ) – miasto w województwie pomorskim , w powiecie wejherowskim nad Zagórską Strugą .
Z miastami Wejherowem i Redą tworzy zespół trzech miast zwany Małym Trójmiastem Kaszubskim .
Wieś duchowna położona była w II połowie XVI wieku w powiecie puckim województwa pomorskiego .
W latach 1945–1975 miasto administracyjnie należało do tak zwanego dużego województwa gdańskiego , a w latach 1975–1998 do tak zwanego małego województwa gdańskiego .
Według danych z 31 grudnia 2021 Rumia liczyła 49 936 mieszkańców .
Jest największym polskim miastem niebędącym siedzibą powiatu .
