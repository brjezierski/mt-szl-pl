Rumunia
Rumunia ( rum .
`` România '' , IPA : // ) – państwo unitarne w południowo-wschodniej części Europy .
Graniczy z Węgrami i Serbią na zachodzie , Bułgarią na południu wzdłuż Dunaju oraz Ukrainą i Mołdawią na północy .
Kraj ma także dostęp do Morza Czarnego .
Stolicą i największym miastem Rumunii jest Bukareszt .
Od 14 grudnia 1955 członek ONZ , od 29 marca 2004 roku Rumunia jest członkiem NATO , a od 1 stycznia 2007 Unii Europejskiej .
Po przyjęciu do UE Rumunia stała się siódmym według liczby ludności krajem wspólnoty .
Geografia .
Znaczna część granicy rumuńsko-bułgarskiej i rumuńsko-serbskiej opiera się o Dunaj .
Dopływ tej rzeki , Prut , tworzy granicę z Mołdawią .
Dunaj wpływa do Morza Czarnego , tworząc deltę , która jest Światowym Rezerwatem Biosfery .
Ponieważ znaczne odcinki rumuńskich granic opierają się o rzeki , często meandrujące , a także ponieważ delta Dunaju stale powiększa się o 2–5 metrów rocznie , powierzchnia Rumunii nieco wzrosła w ostatnich dekadach .
Obecnie powierzchnia kraju wynosi 238 391 km² , podczas gdy w roku 1969 wynosiła około 237 500 km² .
Karpaty stanowią dominującą formę krajobrazu w środkowej Rumunii ( stanowią ponad 30 % całej powierzchni kraju ) i otaczają Wyżynę Transylwańską .
W czterech najwyższych masywach górskich : Retezacie ( do 2509 m n.p.m .
) , Paringu ( do 2518 m ) , Górach Fogaraskich ( do 2544 m ) i Bucegi ( do 2507 m ) położonych w Karpatach Południowych oraz Górach Kelimeńskich i Rodniańskich w Karpatach Wschodnich liczne szczyty przekraczają wysokość 2000 metrów .
Znaczna wysokość gór sprawia , że posiadają one cechy rzeźby wysokogórskiej ( niewielkie kotły polodowcowe , polodowcowe jeziora górskie , ściany skalne ) , przypominające wyglądem Tatry Zachodnie , a w środkowej części Gór Fogaraskich rzeźbę wybitnie wysokogórską z nagimi ścianami skalnymi przypominającymi Tatry Wysokie .
Na południu Karpaty przechodzą w łagodne wzgórza , a następnie w Równinę Bărăgan .
Trzy najwyższe szczyty Rumunii to Moldoveanu ( 2544 m ) , Negoiu ( 2535 m ) i Viștea Mare ( 2527 m ) w Górach Fogaraskich .
