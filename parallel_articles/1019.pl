Podatek akcyzowy
Podatek akcyzowy ( Akcyza , z łac .
`` accisia '' ) – podatek pośredni nakładany na niektóre wyroby konsumpcyjne .
Akcyza jest nakładana na dobra , aby ograniczyć ich spożycie , lub ze względu na wysoką akumulację zysku ( niskie koszty produkcji , a wysokie spożycie ) .
Obecnie stała się obszernym i łatwym wpływem pieniędzy dla budżetu państwa poprzez nakładanie na tzw .
dobra infrastrukturalne ( energia , paliwa itp . )
lub konsumowane na masową skalę ( np .
używki ) .
Cel akcyzy .
Wprowadzenie podatku akcyzowego ma zadanie restrykcyjne .
Dodatkowe obłożenie podatkiem ma za zadanie zmniejszyć popyt na wybrane przez państwo produkty , najczęściej wyroby tytoniowe , napoje alkoholowe , wyroby ropopochodne .
W niektórych przypadkach , kiedy produkt składa się z względnie tanich surowców a jego szkodliwość dla zdrowia jest wysoka , akcyza może stanowić bardzo wysoką część ceny końcowej .
W wielu państwach europejskich akcyza na produkty tytoniowe dochodzi do 75 % ceny końcowej .
