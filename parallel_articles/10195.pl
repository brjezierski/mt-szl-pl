Gwinea Równikowa
Gwinea Równikowa ( hiszp .
`` Guinea Ecuatorial '' ; fr .
`` Guinée Équatoriale '' ; port .
`` Guiné Equatorial '' ) ; oficjalna nazwa państwa to Republika Gwinei Równikowej ( hiszp .
`` República de Guinea Ecuatorial '' ; fr .
`` République de Guinée Équatoriale '' ; port .
`` República da Guiné Equatorial '' ) – państwo położone w centralnej Afryce .
Jest to jedno z najmniejszych państw tego kontynentu oraz jedyne , gdzie językiem urzędowym jest hiszpański .
W czasach kolonialnych występowało pod nazwą Gwinea Hiszpańska .
Graniczy od północy z Kamerunem oraz od południa i wschodu z Gabonem .
Wybrzeża państwa oblewają wody Zatoki Gwinejskiej będącej częścią Oceanu Atlantyckiego .
Wody terytorialne od zachodu dzieli z Wyspami Świętego Tomasza i Książęcą oddalonymi od niej o ok. 230 km .
Terytorium Gwinei Równikowej składa się z dwóch części : kontynentalnej i wyspiarskiej .
Kontynentalna część państwa , zwana Rio Muni lub Mbini , leży na półkuli północnej , a należąca doń wyspa Annobón na południowej .
