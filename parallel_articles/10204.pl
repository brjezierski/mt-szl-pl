SIMC
SIMC ( `` System identyfikatorów i nazw miejscowości '' ) – system będący składnikiem Krajowego Rejestru Urzędowego Podziału Terytorialnego Kraju ( TERYT ) .
System zarządzany jest przez Główny Urząd Statystyczny .
SIMC jest rejestrem nazw miejscowości i ich części , zawierającym następujące informacje :
W bazie SIMC `` miejscowość '' oznacza także część miejscowości podstawowej , na przykład : dzielnicę , osiedle , osadę lub przysiółek .
Siedmiocyfrowy identyfikator SIMC ma cyfrę kontrolną .
System identyfikatorów i nazw miejscowości aktualizowany jest na bieżąco po wprowadzeniu zmian urzędowych nazw miejscowości oraz zmian w zasadniczym podziale terytorialnym państwa .
Identyfikatory miejsc SIMC są unikalne i niezmienne .
Numery SIMC miejscowości likwidowanych nie są powtórnie używane .
