Sigma Ołomuniec
Sportovní klub Sigma Olomouc – czeski klub piłkarski z siedzibą w Ołomuńcu .
Historia .
Klub został założony w 1919 r. jako Hejčín Ołomuniec .
Oprócz wsparcia od Morawskich Hut ważnym wzmocnieniem dla klubu był upadek rywala , SK Ołomuniec ASO w roku 1951 .
Dzięki temu Hejčín zyskał doświadczonych zawodników .
W 1955 r. po zdobyciu mistrzostwa regionu awansowali do ligi .
W 1966 roku sponsorem został koncern Sigma , co pozwoliło na dalszy rozwój drużyny .
Od tego roku klub znany jest jako Sigma Ołomuniec .
Dopiero w 1982 r. Sigma awansowała do I ligi .
Klub zajął jednak ostatnie miejsce i po roku spadł do II ligi .
Ponownie awansował do I ligi w 1984 r. , po czym umocnili swoją pozycję w niej utrzymując się przez 30 lat .
