SV Wehen Wiesbaden
SV Wehen Wiesbaden – niemiecki klub piłkarski z siedzibą w Wiesbaden , założony 1 stycznia 1926 .
Historia .
Klub utworzono 1 stycznia 1926 jako SV Wehen – Taunusstein .
W 1933 został on rozwiązany przez nazistowski rząd III Rzeszy i do 1939 drużyna rozgrywała tylko okazjonalne mecze towarzyskie .
Sytuacja ustabilizowała się dopiero po zakończeniu II wojny światowej .
SV Wehen Wiesbaden nigdy nie wywalczyło awansu do najwyższej klasy rozgrywek w kraju .
W latach 1927–2007 klub występował na `` Stadion am Halberg '' , zaś od 11 października 2007 swe domowe mecze rozgrywa na nowo wybudowanej `` Brita-Arenie '' .
