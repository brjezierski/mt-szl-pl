Saarbrücken
Saarbrücken ( fr .
`` Sarrebruck '' ; luks .
`` Saarbrécken '' ) – miasto w Niemczech nad Saarą , stolica kraju związkowego ( niem .
`` Bundesland '' ) Saara , siedziba związku regionalnego Saarbrücken .
Polityczne i kulturalne centrum regionu , główny ośrodek gospodarczy Zagłębia Saary , wydobycie węgla kamiennego , hutnictwo żelaza , przemysł maszynowy , metalowy , elektrotechniczny , precyzyjny .
Znajdują się tu Muzeum Hutnictwa i Muzeum Przemysłu , lotnisko i uniwersytet .
Obecna aglomeracja powstała w 1909 przez połączenie trzech miast : Saarbrücken , St. Johann oraz Malstatt-Burbach i liczy 175 741 mieszkańców ( 2010 ) , w tym wielu obcokrajowców .
Nazwa .
Nazwa Saarbrücken ( pierwsza wzmianka jako `` Sarabrucca '' ) nie pochodzi od mostu ( niem .
Brücke , Brücken w l.m . )
powyżej Saary , jak mogłoby się wydawać , ponieważ pierwsze wzmianki pochodzą z czasów gdy nie było jeszcze na rzece Saarze mostu ( pierwszy most , Alte Brücke został zbudowany 500 lat po pierwszej wzmiance tej nazwy ) .
W rzeczywistości nazwa pochodzi prawdopodobnie od twierdzy na skale ( = Brocken ) co dało określenie Sarabrucca .
Na tej skale stoi obecnie Zamek Saarbruecken .
