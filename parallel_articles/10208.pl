Sabaton
Sabaton – szwedzka grupa wykonująca muzykę z pogranicza power i heavy metalu .
Tematyka większości utworów jest powiązana z wojną .
Powstała w 1999 w Falun .
Do 2022 zespół wydał dziesięć albumów studyjnych niejednoznacznie ocenianych przez krytyków muzycznych .
W 2006 i 2007 roku uznana za najlepszy zespół hardrockowy w Szwecji .
Grupa wielokrotnie występowała w Polsce .
Historia .
Grupa powstała w 1999 roku w Falun z inicjatywy wokalisty i klawiszowca Joakima Brodéna , gitarzystów Rikarda Sundéna i Oskara Monteliusa , basisty Pära Sundströma oraz perkusisty Richarda Larssona .
Nazwa zespołu pochodzi od angielskiej nazwy trzewików , czyli części pełnej zbroi płytowej osłaniająca stopę .
Zimą 2000 roku w studiu Abyss muzycy zarejestrowali kilka utworów .
Tego samego roku nakładem Underground Symphony ukazała się kompilacja pt .
