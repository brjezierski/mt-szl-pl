Akita ( dworzec kolejowy )
– dworzec kolejowy w mieście Akita , w prefekturze Akita , w Japonii .
Położenie .
Dworzec znajduje się w centrum miasta , w dzielnicy Nakadori w kwartale 7-chōme , w pobliżu parku Senshū Kōen .
Linie kolejowe .
Akita jest węzłem kilku linii kolejowych .
Przebiega przez nią linia Ōu-honsen .
Ponadto stanowi ona punkt końcowy szybkiej kolei Akita Shinkansen oraz linii Uetsu-honsen .
Dla linii Oga-sen jest punktem początkowym .
Historia .
Stacja została otwarta 20 października 1902 roku przez Japan National Railway .
16 marca 1997 roku ukończono budowę nowego dworca .
22 marca tego roku zaczął kursować Akita Shinkansen .
Statystyki .
W 2012 roku dworzec ten obsługiwał średnio 11 143 pasażerów dziennie .
