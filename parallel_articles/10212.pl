Sadiq Khan
Sadiq Aman Khan ( ; ur .
8 października 1970 w Londynie ) – brytyjski polityk i prawnik pakistańskiego pochodzenia , burmistrz Londynu od 2016 i pierwszy w historii muzułmanin pełniący ten urząd , członek Izby Gmin z londyńskiej dzielnicy Tooting w trzech kolejnych kadencjach ( 2005 , 2010 , 2015 ) .
Członek brytyjskiej Partii Pracy .
Ideowo znajduje się na umiarkowanym , socjaldemokratycznym skrzydle partii .
Życiorys .
Dzieciństwo i młodość .
Urodził się w szpitalu Świętego Jerzego w Tooting , w południowym Londynie jako piąte z ośmiorga dzieci ( siedmiu synów i córka ) w rodzinie pakistańskich imigrantów .
Jego dziadkowie przenieśli się z Indii do Pakistanu po podziale Indii w 1947 , a krótko przed jego urodzinami jego rodzice przenieśli się do Wielkiej Brytanii .
Jego zmarły ojciec , Amanullah Khan , pracował przez ponad 25 lat jako kierowca londyńskiego autobusu ; jego matka , Sehrun , była szwaczką .
Wielokrotnie wspominał , że „ mama i tata byli cały czas w pracy , więc jak tylko mogłem się o to ubiegać i ja zacząłem pracować .
Rozwoziłem gazety , a czasami pracowałem na budowie ” . ” Jego rodzina nadal wysyła pieniądze do krewnych w Pakistanie , „ bo jesteśmy błogosławieni , mogąc być w Wielkiej Brytanii ” . ”
Wraz z rodzeństwem dorastał w trzypokojowym mieszkaniu socjalnym na osiedlu imienia księcia Henry ’ ego w Earlsfield .
Skończył szkołę podstawową Fircroft i liceum Ernest Bevin .
Zdał maturę z biologii i matematyki , mając nadzieję na karierę jako stomatolog .
