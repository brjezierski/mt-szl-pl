Sadkowo ( gmina )
Sadkowo – dawna gmina wiejska istniejąca w latach 1946–1954 w woj .
szczecińskim i koszalińskim ( dzisiejsze woj .
zachodniopomorskie ) .
Siedzibą władz gminy było Sadkowo .
Gmina Sadkowo powstała po II wojnie światowej na terenie tzw .
Ziem Odzyskanych ( tzw .
III okręg administracyjny – `` Pomorze Zachodnie '' ) .
28 czerwca 1946 gmina – jako jednostka administracyjna powiatu białogardzkiego – weszła w skład nowo utworzonego woj .
szczecińskiego .
6 lipca 1950 gmina wraz z całym powiatem białogardzkim weszła w skład nowo utworzonego woj .
koszalińskiego .
Według stanu z 1 lipca 1952 gmina składała się z 8 gromad : Doble , Łyśnica , Motarzyno , Osówko , Ostre Bardo , Radzewo , Sadkowo i Stare Dębno .
Gmina została zniesiona 29 września 1954 wraz z reformą wprowadzającą gromady w miejsce gmin .
Jednostki nie przywrócono 1 stycznia 1973 wraz z kolejną reformą reaktywującą gminy .
