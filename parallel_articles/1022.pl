Akka ( stacja kolejowa )
Akka ( hebr .
: עכו ) – stacja kolejowa w mieście Akka , w Izraelu .
Jest obsługiwana przez Rakewet Jisra ’ el .
Stacja znajduje się we wschodniej części miasta Akka .
W latach 2001-2002 dworzec przeszedł gruntowną przebudowę , podczas której powstała nowoczesna hala dworcowa , nowa platforma na perony i tunel łączący dwa perony .
Dworzec jest przystosowany dla osób niepełnosprawnych .
Połączenia .
Pociągi z Akki jadą do Naharijji , Hajfy , Tel Awiwu , Lod , Modi ’ in , Rechowot , Aszkelonu i Beer Szewy .
