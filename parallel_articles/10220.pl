Safakis
Safakis ( , `` Şafāqiş '' , ) – miasto portowe na wschodnim wybrzeżu Tunezji , ośrodek administracyjny gubernatorstwa Safakis , nad zatoką Mała Syrta , 270 km na południowy wschód od Tunisu .
W 2014 roku mieszkały w nim około 273 tysiące osób ( 2013 ) .
Drugie co do wielkości miasto kraju .
W mieście rozwinął się przemysł chemiczny , spożywczy , włókienniczy , metalowy , obuwniczy oraz rzemieślniczy .
