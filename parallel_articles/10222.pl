Safona ( imię )
Safona – żeńskie imię pochodzenia greckiego .
Imię pochodzi od najsławniejszej poetki starożytnej Grecji z przełomu VII i VI wieku p.n.e .
o imieniu Psafo , Sapfo lub Saffo , a w polskiej pisowni Safona .
W innych językach to np .
Sappho , Szappho lub Sapfö .
