Said Belqola
Said Belqola ( arab .
سعيد بلقولة , ur .
30 sierpnia 1956 w Tiflecie , zm .
15 czerwca 2002 w Rabacie ) – były marokański sędzia piłkarski , znany z sędziowania finałowego meczu Mistrzostw Świata 1998 pomiędzy Brazylią a Francją ( 0:3 ) , będąc tym samym pierwszym afrykańskim sędzią prowadzącym mecz finałowy Mundialu .
Życiorys .
Belqola karierę międzynarodową rozpoczął w 1993 roku prowadząc mecze rozgrywek klubowych w Afryce .
Był sędzią m.in .
meczów Mistrzostw Świata U-17 w Piłce Nożnej 1995 rozgrywanych w Ekwadorze , Pucharu Narodów Afryki 1996 rozgrywanych w Południowej Afryce .
Prowadził mecz otwarcia pomiędzy RPA a Kamerunem , wygrany przez `` Nieposkromione Lwy '' ( 3:0 ) .
Sędziował również mecze Pucharu Narodów Afryki 1998 rozgrywanych w Burkina Faso .
Łącznie w Pucharze Narodów Afryki sędziował w 4 meczach .
W roku 1998 znalazł w gronie arbitrów mających sędziować mecze Mistrzostwach Świata we Francji .
Zadebiutował 15 czerwca 1998 roku na Parc des Princes prowadząc mecz Niemców z USA ( 2:0 ) , następnie 26 czerwca mecz Argentyny a Chorwacją ( 1:0 ) i 12 lipca b.r .
finałowy mecz pomiędzy Brazylią a Francją ( 0:3 ) .
W tym meczu w wyniku dwóch żółtych kartek pokazał w konsekwencji czerwoną kartkę francuskiemu obrońcy Marcelowi Desailly'emu .
Belqola urodził się Tiflecie i pracował Fezie jako urzędnik państwowy i celnik .
Belqola zmarł 15 czerwca 2002 w szpitalu wojskowowym im .
Muhammada V w Rabacie po długiej walce z rakiem .
Został pochowany w Tiflecie .
