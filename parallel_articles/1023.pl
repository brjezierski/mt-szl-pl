Akra ( Krym )
Akra – starożytne miasto Greków bosporańskich we wschodniej części Krymu .
Miasto to należało do tzw .
małych miast bosporańskich i istniało w okresie od V wieku p.n.e .
do IV wieku n.e .
Położenie .
Ruiny miasta położone są na Półwyspie Kerczeńskim nad Cieśniną Kerczeńską ( w starożytności zwanej Bosporem Kimmeryjskim ) , w pobliżu współczesnej wsi Nabiereżnoje , 3 km na północ od przylądka Takil , najbardziej na południowy wschód wysuniętego punktu Półwyspu Kerczeńskiego .
Znajdują się na mierzei oddzielającej niewielkie , przybrzeżne jezioro Janysz od brzegu morza , przy czym główna część miasta jest zatopiona w wodach jeziora i morza .
Starożytni o Akrze .
Wspominane było przez licznych autorów starożytnych jako miasto ( przez Pliniusza Starszego , Klaudiusza Ptolemeusza , Stefana Bizantyjskiego ) , osada ( przez Strabona ) lub nawet wieś ( przez Psudo-Arriana ) .
Skupiało ono uwagę starożytnych na sobie ze względu na swoje położenie geograficzne , miało bowiem leżeć w najdalszym południowo-zachodnim punkcie Cieśniny Kerczeńskiej , miejscu gdzie brzeg Półwyspu Kerczeńskiego skręca na zachód .
Poszukiwania miasta .
O mieście w kontekście poszukiwań mówiło się już w XVIII wieku i dwukrotnie ogłaszano jego odkrycie .
Odkryte na początku XIX wieku ruiny okazały się , po przypadkowym odkryciu przez rybaków kamiennej płyty z nazwą miasta w 1918 , bosporańskim miastem Kytaja ( łac .
Cytaea ) .
