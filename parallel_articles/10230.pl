Saiki
– miasto położone w Japonii , w prefekturze Ōita , we wschodniej części wyspy Kiusiu .
Miasto zostało założone 29 kwietnia 1941 roku .
3 marca 2005 roku miasteczka Kamae , Kamiura , Tsurumi , Ume i Yayoi oraz wsie Honjō , Naokawa i Yonōzu , będące częścią powiatu Minamiamabe , zostały włączone do miasta , a powiat został rozwiązany .
