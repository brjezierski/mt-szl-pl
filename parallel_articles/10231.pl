Historia .
Pierwsze wzmianki na temat Saingin-en-Melantois pojawiły się w oficjalnych dokumentów w sierpniu 977 roku .
Wówczas miejscowość ta miała drewnianą zabudowę a miastem jako pierwszy zarządzał Rogier de Sengin .
Obecnie w centrum znajduje się zabytkowy kościół .
