Geografia .
Ze względu na wulkaniczne pochodzenie wyspy są górzyste .
Najwyższe wzniesienie na wyspie Saint Kitts to wulkan Mount Liamuiga ( 1156 m n.p.m .
) , a na wyspie Nevis góry osiągają wysokość do 985 m ( Nevis Peak ) .
W południowej części wyspy St. Kitts rozciąga się długi półwysep ze słonym jeziorem .
Wyspy dzieli cieśnina Narrows .
Wzdłuż wybrzeży wysp ciągną się rafy koralowe .
