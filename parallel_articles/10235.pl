Saint Kitts i Nevis
Saint Kitts i Nevis , Federacja Saint Kitts i Nevis ( ang .
`` Saint Kitts and Nevis '' , `` Federation of Saint Kitts and Nevis '' ) ; także jako Saint Christopher i Nevis , Federacja Saint Christopher i Nevis ( ang .
`` Saint Christopher and Nevis '' , `` Federation of Saint Christopher and Nevis '' ) – państwo w Ameryce Środkowej na Morzu Karaibskim .
Ustrój polityczny .
Saint Kitts i Nevis jest oficjalnie monarchią konstytucyjną .
Obowiązuje konstytucja z 19 września 1983 .
Głową państwa jest brytyjski monarcha reprezentowany przez gubernatora generalnego .
Władza ustawodawcza należy do parlamentu składającego się z 14 deputowanych wybieranych na 5-letnią kadencję .
11 wybiera się w wyborach powszechnych , a 3 jest mianowanych przez gubernatora ( 2 na wniosek premiera i 1 lidera opozycji ) .
Władzą wykonawczą jest rząd .
Członkowie są mianowani przez gubernatora spośród deputowanych parlamentu .
Wyspa Nevis ma własne władze ustawodawcze i wykonawcze .
Główne partie polityczne : Partia Pracy Saint Kitts i Nevis , Ruch Akcji Ludowej , Ludowa Partia Pracy .
