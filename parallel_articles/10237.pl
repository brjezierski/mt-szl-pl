Saint Lucia
– państwo w Ameryce Środkowej na Morzu Karaibskim , na wyspie o tej samej nazwie należącej do archipelagu Wysp Nawietrznych , części Małych Antyli .
Ustrój polityczny .
Monarchia konstytucyjna , członek brytyjskiej Wspólnoty Narodów .
Konstytucja została uchwalona w 1979 .
Głową państwa jest monarcha brytyjski , reprezentowany przez mianowanego przez siebie gubernatora generalnego .
Władza ustawodawcza należy do 2-izbowego parlamentu , złożonego z 11-osobowego senatu , mianowanego przez gubernatora i 17-osobowej Izby Zgromadzenia , wybieranej na 5 lat w wyborach powszechnych .
Władza wykonawcza należy do rządu powoływanego przez gubernatora na wniosek premiera i odpowiedzialnego przed Izbą Zgromadzenia .
Na premiera gubernator powołuje przywódcę większości w Izbie Zgromadzenia .
