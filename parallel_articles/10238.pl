Saint Michael ( parafia )
St. Michael – parafia w południowo-zachodniej części Barbadosu .
Największym miastem jest Bridgetown – stolica Barbadosu .
Bridgetown jest centrum handlowym , usługowym i przemysłowym wyspy .
Parafia jest siedzibą kilku szkół , włączając w to `` Combermere '' i prestiżowy Harrison College , rozmaitych instytucji rządowych np .
Ministerstwa Edukacji czy Ministerstwa Przemysły i Handlu Zagranicznego .
W St. Michael znajduje się międzynarodowy port morski Barbadosu – Deep Water Harbour .
Wielka część ruchu turystycznego przybywa na Barbados właśnie przez ten port , statkami wycieczkowymi takich linii jak : Royal Carribean czy Cunard .
W porcie znajduje się także kilka żurawi do ładowania wyprodukowanego na wyspie cukru i jeden żuraw do ładowania mąki .
Z St. Michael pochodzi piosenkarka Rihanna .
