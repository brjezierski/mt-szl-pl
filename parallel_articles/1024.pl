Akranes
Akranes – miasto na zachodnim wybrzeżu Islandii , tworzące gminę Akraneskaupstaður , wchodzącą w skład regionu Vesturland .
Ulokowane jest na zachodnim krańcu półwyspu położonego między fiordami Hvalfjörður i Grunnarfjörður .
Nad miastem dominuje góra Akrafjall ( 643 m n.p.m . ) .
W okolicach miasta znajduje się również prawie 6 kilometrowy tunel podwodny przeprowadzony pod fiordem Hvalfjörður łączący Mosfellsbær i Akranes .
Na początku 2018 roku zamieszkiwało je 7249 osób - stanowi tym samym największą pod względem liczby ludności miejscowość w regionie Vesturland .
