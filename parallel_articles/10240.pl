Saint Vincent i Grenadyny
Saint Vincent i Grenadyny ( ang .
`` Saint Vincent and the Grenadines '' ) – wyspiarskie państwo na Morzu Karaibskim , w archipelagu Małych Antyli .
Oprócz wyspy Saint Vincent obejmuje położoną na południe od niej północną część archipelagu Grenadyny ( jego południowa część należy do Grenady ) .
Ustrój polityczny .
Saint Vincent i Grenadyny jest monarchią , wchodzącą w skład brytyjskiej Wspólnoty Narodów od czasu uzyskania niepodległości w 1979 roku .
Głową państwa jest król brytyjski reprezentowany przez gubernatora generalnego .
Władzę ustawodawczą reprezentuje jednoizbowy parlament .
Władza wykonawcza znajduje się w rękach rządu .
