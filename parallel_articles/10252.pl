Saint-Cloud
Saint-Cloud – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Mieści się na zachodnich przedmieściach Paryża , niecałe 10 kilometrów od centrum .
Według danych na rok 2012 gminę zamieszkiwało 29 436 osób , a gęstość zaludnienia wynosiła 3 894 osób/km² .
Wśród 1287 gmin regionu Île-de-France Saint-Cloud plasuje się na 516. miejscu pod względem powierzchni .
9 listopada 1799 roku ( 18 brumaire ’ a wedle kalendarza rewolucyjnego ) obradowała w pałacu Saint-Cloud Rada Pięciuset , niższa izba parlamentu , rozwiązana 10 listopada na skutek przewrotu 18 brumaire ’ a generała Napoleona Bonaparte , który został wkrótce potem wybrany pierwszym konsulem i objął pełną władzę we Francji .
