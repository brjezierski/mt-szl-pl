Saint-Cyr-l ’ École
Saint-Cyr-l ’ École – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Yvelines .
Znajduje się w odległości 24 km od Paryża , w pobliżu Wersalu .
