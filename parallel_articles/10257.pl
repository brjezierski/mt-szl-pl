Saint-Dizier-Masbaraud
Saint-Dizier-Masbaraud – gmina we Francji , w regionie Nowa Akwitania , w departamencie Creuse .
W 2013 roku populacja ludności wynosiła 1208 mieszkańców .
Gmina została utworzona 1 stycznia 2019 roku z połączenia dwóch ówczesnych gmin : Masbaraud-Mérignat oraz Saint-Dizier-Leyrenne .
Siedzibą gminy została miejscowość Saint-Dizier-Leyrenne .
