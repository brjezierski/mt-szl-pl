Akureyri
Akureyri – miasto w północnej Islandii , położone nad fiordem Eyjafjörður w otoczeniu granitowych gór dochodzących do 1500 m wysokości .
Drugi co do wielkości po Reykjaviku obszar miejski Islandii , tworzący gminę Akureyrarkaupstaður .
Znaczący port morski i lotniczy .
Z miasta pochodzi około 30 % islandzkiego przetwórstwa rybnego .
Ośrodek handlowy , usługowy i komunikacyjny w północnej Islandii .
Znajduje się tu drugi , obok Uniwersytetu Islandzkiego , uniwersytet w kraju .
Pierwszym osadnikiem miasta był norwesko-irlandzki Helgi Magri ( Helgi Chudy ) .
Akureyri jako stacja handlowa było wzmiankowane już w 1602 roku .
