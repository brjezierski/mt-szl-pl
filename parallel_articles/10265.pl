Saint-Geniez-d ’ Olt
Saint-Geniez-d ’ Olt – miejscowość i dawna gmina we Francji , w regionie Oksytania , w departamencie Aveyron .
W dniu 1 stycznia 2016 roku z połączenia sześciu ówczesnych gmin – Aurelle-Verlac oraz Saint-Geniez-d ’ Olt – powstała nowa gmina Saint Geniez d ’ Olt et d ’ Aubrac .
Siedzibą gminy została miejscowość Saint-Geniez-d ’ Olt .
W 2013 roku populacja Saint-Geniez-d ’ Olt wynosiła 2018 mieszkańców .
