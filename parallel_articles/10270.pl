Saint-Germain-d ’ Ectot
Saint-Germain-d ’ Ectot – miejscowość i dawna gmina we Francji , w regionie Normandia , w departamencie Calvados .
W 2013 roku populacja ludności wynosiła 304 mieszkańców .
W dniu 1 stycznia 2017 roku z połączenia czterech ówczesnych gmin – Anctoville , Longraye , Saint-Germain-d ’ Ectot oraz Torteval-Quesnay – utworzono nową gminę Aurseulles .
Siedzibą gminy została miejscowość Anctoville .
