Saint-Germain-en-Laye
Saint-Germain-en-Laye – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Yvelines , na zachód od Paryża .
W 2016 roku populacja ówczesnej gminy wynosiła 41 749 mieszkańców .
Miasto jest siedzibą Międzynarodowego Stowarzyszenia Służb Oznakowania Nawigacyjnego ( IALA ) .
Historia .
Między 996 a 1031 rokiem Robert II Pobożny założył w tym miejscu klasztor .
W czerwcu 1679 zawarto w Saint-Germain-en-Laye pokój , na mocy którego Szwecja odzyskała Szczecin od Brandenburgii .
W Saint-Germain , we wrześniu 1919 podpisany został traktat pokoju z Austrią .
Czechosłowacji przyznano Morawy , rdzenne Czechy i część Śląska , Polsce natomiast Galicję i część Śląska Cieszyńskiego .
Dnia 1 stycznia 2019 roku połączono dwie wcześniejsze gminy : Fourqueux oraz Saint-Germain-en-Laye .
Siedzibą gminy została miejscowość Saint-Germain-en-Laye , a nowa gmina przyjęła jej nazwę .
