Saint-Jean-de-Linières
Saint-Jean-de-Linières – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku populacja ówczesnej gminy wynosiła 1807 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia dwóch ówczesnych gmin – Saint-Jean-de-Linières oraz Saint-Léger-des-Bois – powstała nowa gmina Saint-Léger-de-Linières .
Siedzibą gminy została miejscowość Saint-Léger-des-Bois .
