Saint-Jores
Saint-Jores – miejscowość i dawna gmina we Francji , w regionie Normandia , w departamencie Manche .
W 2013 roku jej populacja wynosiła 368 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia czterech ówczesnych gmin – Coigny , Lithaire , Prétot-Sainte-Suzanne oraz Saint-Jores – utworzono nową gminę Montsenelle .
Siedzibą gminy została miejscowość Lithaire .
