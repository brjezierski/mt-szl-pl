Saint-Julien-de-Crempse
Saint-Julien-de-Crempse – miejscowość i dawna gmina we Francji , w regionie Nowa Akwitania , w departamencie Dordogne .
W 2013 roku populacja ówczesnej gminy wynosiła 227 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia czterech ówczesnych gmin – Maurens , Laveyssière , Saint-Jean-d'Eyraud oraz Saint-Julien-de-Crempse – powstała nowa gmina Eyraud-Crempse-Maurens .
Siedzibą gminy została miejscowość Maurens .
