Saint-Malo
Saint-Malo ( bret .
`` Sant-Maloù '' ) – otoczona murem miejscowość portowa i gmina we Francji , w regionie Bretania , w departamencie Ille-et-Vilaine , położona na wschodnim brzegu rzeki Rance , nad jej ujściem do kanału La Manche .
Według danych na rok 1990 gminę zamieszkiwało 48 057 osób , a gęstość zaludnienia wynosiła 1314 osób/km² ( wśród 1269 gmin Bretanii Saint-Malo plasuje się na 5. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 179 .
) .
Historia .
Miasto rozwinęło się przy klasztorze założonym w VI wieku przez walijskiego mnicha Maclo ( Malo ) .
W XII wieku otrzymała prawa miejskie .
W latach 1144–1790 siedziba biskupstwa .
Przedmiot sporów między biskupami popieranymi przez królów francuskich a książętami Bretanii sprzyjającymi Anglikom .
W średniowieczu duży ośrodek handlowy .
W 1491 przyłączone do Korony francuskiej .
Miasto było bazą wypraw do Ameryki Północnej , w XVIII wieku podupadło .
W Saint-Malo urodzili się korsarze : Jacques Cartier ( odkrywca i badacz wielu regionów Kanady ) , René Duguay-Trouin i Robert Surcouf .
Podczas II wojny światowej bardzo zniszczone .
Ośrodek przemysłu spożywczego ( przetwórstwo ryb ) .
Węzeł komunikacyjny ( połączenie promowe z Wielką Brytanią ) .
Port rybacki .
W pobliżu pierwsza na świecie elektrownia pływowa ( 240 megawatów ) zbudowana w 1966 .
Ośrodek turystyczny i kąpielisko morskie .
