Saint-Martin-le-Hébert
Saint-Martin-le-Hébert – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku jej populacja wynosiła 156 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia sześciu ówczesnych gmin – Bricquebec , Les Perques , Quettetot , Saint-Martin-le-Hébert , Le Valdécie oraz Le Vrétot – utworzono nową gminę Bricquebec-en-Cotentin .
Siedzibą gminy została miejscowość Bricquebec .
