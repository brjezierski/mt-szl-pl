Saint-Martin-lès-Seyne
Saint-Martin-lès-Seyne – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 15 osób , a gęstość zaludnienia wynosiła 1 osób/km² .
W styczniu 2015 r. Saint-Martin-lès-Seyne zamieszkiwało 17 osób , przy gęstości zaludnienia wynoszącej 1,4 osób/km² .
