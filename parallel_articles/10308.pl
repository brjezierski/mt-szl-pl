Saint-Pierre ( wyspa w Saint-Pierre i Miquelon )
Saint-Pierre – mniejsza z dwóch głównych wysp Saint-Pierre i Miquelon , zamorskiego terytorium Francji , leżącego na Oceanie Atlantyckim niedaleko wybrzeży Kanady .
Wraz z kilkoma małymi wyspami w pobliżu ( m.in .
Île aux Marins ) tworzy gminę Saint-Pierre , zamieszkaną przez 5623 osoby ( 2017 ) .
Powierzchnia wyspy wynosi około 26 km² .
Główną miejscowością na wyspie jest Saint-Pierre , stolica terytorium .
Najwyższą górą jest Le Trépied , wznosząca się na 207 m n.p.m .
Transport .
Na Saint-Pierre znajduje się międzynarodowy port lotniczy Saint-Pierre , obsługiwany przez linie lotnicze Air Saint-Pierre z połączeniami m.in .
do Halifaksu , Montrealu , na wyspę Miquelon i do portu lotniczego St. John ’ s w Nowej Fundlandii .
