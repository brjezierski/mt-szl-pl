Saint-Quentin
Saint-Quentin – miejscowość i gmina we Francji , w regionie Hauts-de-France , w departamencie Aisne .
Historia .
Miejsce urodzenia Szymona z Saint-Quentin ( XIII w. ) oraz François Babeufa ( 1760 ) .
Miasto było historyczną stolicą hrabstwa Vermandois ( od IX do XIII w. ) .
