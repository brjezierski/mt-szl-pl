Saint-Yorre
Saint-Yorre – miejscowość i gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Allier .
Demografia .
Według danych na rok 1990 gminę zamieszkiwały 3003 osoby , a gęstość zaludnienia wynosiła 473 osoby/km² .
W styczniu 2015 r. Saint-Yorre zamieszkiwały 2852 osoby , przy gęstości zaludnienia wynoszącej 449,1 osób/km² .
