Saint-Étienne-du-Grès
Saint-Étienne-du-Grès - miejscowość i gmina we Francji .
Informacje .
Saint-Étienne-du-Grès mieści się w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Delta Rodanu .
Według danych na rok 2006 gminę zamieszkiwały 2111 osoby , a gęstość zaludnienia wynosiła 72 osoby na km² .
Wśród 963 gmin regionu Prowansja-Alpy-Lazurowe Wybrzeże Saint-Étienne-du-Grès plasuje się na 279. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 348 .
Od marca 2001 roku urząd burmistrza miasta obejmuje Robert del Testa .
