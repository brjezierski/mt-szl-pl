Sainte-Alvère
Sainte-Alvère – miejscowość i dawna gmina we Francji , w regionie Nowa Akwitania , w departamencie Dordogne .
W 2013 roku populacja Sainte-Alvère wynosiła 863 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Sainte-Alvère oraz Saint-Laurent-des-Bâtons – utworzono nową gminę Sainte-Alvère-Saint-Laurent Les Bâtons .
Siedzibą gminy została miejscowość Sainte-Alvère .
Od 1 stycznia 2017 roku znajduje się w gminie Val-de-Louyre-et-Caudeau , której jest siedzibą .
