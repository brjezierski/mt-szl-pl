Sainte-Anne-d ’ Auray
Sainte-Anne-d ’ Auray ( bret .
`` Santez-Anna-Wened '' ) – miejscowość i gmina we Francji , w regionie Bretania , w departamencie Morbihan .
Według danych na rok 1990 gminę zamieszkiwało 1630 osób , a gęstość zaludnienia wynosiła 328 osób/km² ( wśród 1269 gmin Bretanii Sainte-Anne-d ’ Auray plasuje się na 387. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 1022 .
) .
Na terenie miejscowości znajduje się sanktuarium świętej Anny , wzniesione tam w XVII wieku , po serii objawień , jakie według tradycji miał doświadczyć Iwo Nicolazic .
Sanktuarium do dnia dzisiejszego jest celem licznych pielgrzymek .
