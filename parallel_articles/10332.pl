Sainte-Tulle
Sainte-Tulle – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 2855 osób , a gęstość zaludnienia wynosiła 167 osób/km² .
W styczniu 2015 r. Sainte-Tulle zamieszkiwały 3524 osoby , przy gęstości zaludnienia wynoszącej 206,1 osób/km² .
