Sakaiminato
– miasto w Japonii , w prefekturze Tottori , w południowo-zachodniej części wyspy Honsiu , port rybacki nad Morzem Japońskim .
Położenie .
Miasto leży we wschodniej części prefektury , na końcu półwyspu Yumigahama .
Miasto graniczy z Yonago w Tottori oraz Matsue i Yasugi w prefekturze Shimane .
Historia .
W wyniku połączenia w 1954 roku okolicznych wsi i miejscowości , powstało miasteczko Sakaiminato .
1 kwietnia 1956 roku zdobyło status miasta .
