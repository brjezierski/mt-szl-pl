Sakata
– miasto w Japonii ( Honsiu ) , nad Morzem Japońskim , w prefekturze Yamagata .
Ma powierzchnię 602,97 km2 .
W 2020 r. mieszkało w nim 100 320 osób , w 39 425 gospodarstwach domowych ( w 2010 r. 111 170 osób , w 38 944 gospodarstwach domowych ) .
Położenie .
Miasto leży w północnej części prefektury nad Morzem Japońskim .
