Saksonia
Saksonia , Wolny Kraj Saksonia ( łac .
`` Saxonia '' ; niem .
`` Freistaat Sachsen '' ; górnołuż .
`` Swobodny stat Sakska '' ; dolnołuż .
`` Lichotny stat Sakska '' ) – kraj związkowy w Niemczech graniczący z Polską , Czechami i z następującymi krajami związkowymi : Bawaria , Turyngia , Saksonia-Anhalt i Brandenburgia .
Stolicą jest Drezno , a największym miastem Lipsk .
Łużyce ( prawie cała kraina historyczna , z wyjątkiem obszaru wokół Chociebuża ) należały do Elektoratu Saksonii w latach 1635–1815 .
Zostały one przyznane jako nagroda za opowiedzenie się protestanckiego elektoratu Saksonii w wojnie trzydziestoletniej po stronie katolickiego cesarza .
Okolice Bogatyni ( skrajnie południowo-wschodnia część Łużyc ) należały do Saksonii aż do 1945 roku – ten niewielki skrawek znajduje się dzisiaj w Polsce , w województwie dolnośląskim .
Jej obszar do 1989 r. wchodził w skład Niemieckiej Republiki Demokratycznej jako osobne obszary administracyjne ( `` Bezirk '' ) .
Została odtworzona w 1990 roku po zjednoczeniu Niemiec .
Obejmuje ona w przybliżeniu obszar dawnego elektoratu , a później Królestwa Saksonii .
Pod względem powierzchni Saksonia jest dziesiątym , a pod względem liczby ludności , szóstym spośród 16 niemieckich krajów związkowych .
Wschodnia Saksonia obejmuje część Łużyc ( niem .
`` Lausitz '' ) .
Obszar ten zamieszkuje mniejszość – Serbołużyczanie , dlatego też wiele tablic informacyjnych jest dwujęzycznych ( w języku niemieckim i górnołużyckim ) .
Nauczanie odbywa się również w tym języku .
Geografia .
Poza stolicą innymi głównymi miastami Saksonii są : Lipsk , Chemnitz , Zwickau , Plauen i Görlitz .
Główne rzeki to : Łaba , Mulda , Biała Elstera oraz tworząca polsko-niemiecką granicę Nysa Łużycka .
Obszar Saksonii wznosi się stopniowo z północy ku południu , przechodząc nad granicą z Czechami w góry .
Od Bawarii do Łaby rozciągają się Rudawy , w których Łaba utworzyła imponujący przełom ( tzw .
Saska Szwajcaria ) .
Na wschód od Łaby znajdują się Góry Łużyckie należące do łańcucha Sudetów .
Historia .
Archeologia .
W okresie przedhistorycznym obszar współczesnej Saksonii zajmowała archeologiczna kultura łużycka ze środkowej i młodszej epoki brązu oraz wczesnej epoki żelaza .
Swoim największym zasięgiem objęła ona niemal całe ziemie polskie , z wyjątkiem północno-wschodnich skrawków kraju , północno-zachodnią i środkową Słowację , północne i środkowe Morawy , północną i północno-wschodnią część Czech , Saksonię , Łużyce , wschodnią Turyngię oraz wschodnią Brandenburgię .
Państwo Sasów i wyodrębnienie się Saksonii-Wittenbergi .
Współcześni mieszkańcy Saksonii nie są dosłownie potomkami niemieckiego szczepu o tej samej nazwie , którego nazwa wywodzi się od germańskiego plemienia Sasów .
Gniazdem rodowym Sasów była dzisiejsza Dolna Saksonia .
We wczesnym średniowieczu termin „ Saksonia ” odnosił się do innego regionu , obejmującego dzisiejsze kraje związkowe Dolna Saksonia , Brema i północną część Nadrenii Północnej-Westfalii .
Pierwsze księstwo Saksonii pojawiło się około roku 900 na terenie dzisiejszej Dolnej Saksonii i Nadrenii Północnej-Westfalii .
Ówcześni władcy tego państwa byli cesarzami rzymskimi ( Ottonowie ) tworząc tzw .
dynastię saską .
W roku 1137 władzę w Saksonii objęli Welfowie .
Kraj osiągnął największe rozmiary za panowania Henryka Lwa , ale po jego śmierci Saksonia zaczęła słabnąć i w roku 1180 znaczne obszary na zachód od Łaby przeszły pod panowanie biskupów z Kolonii .
Resztki przeszły pod panowanie pomniejszych dynastii niemieckich książąt i zostały podzielone w 1260 roku na Saksonię-Lauenburg i Saksonię-Wittenbergię .
