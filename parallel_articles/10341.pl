Salala
Salala ( arab .
صلالة ) – miasto w południowo-zachodnim Omanie , nad Morzem Arabskim ( częścią Oceanu Indyjskiego ) , stolica Muhafazy Zufar i siedziba administracyjna wilajetu Salala .
W latach 1932–1970 stolica Omanu .
Według danych z 2021 r. liczyło 163 140 mieszkańców ( 3. miejsce pod tym względem w kraju ) .
Obok miasta przebiega ważny dla Omanu szlak komunikacyjny : droga Maskat – Salala .
W tym mieście urodził się poprzedni sułtan Omanu , Kabus ibn Sa ’ id .
