Salerno ( stacja kolejowa )
( wym .
, pol .
uproszczona : `` salerno '' ) – stacja kolejowa w Salerno , w regionie Kampania , we Włoszech .
Stacja znajduje się u zbiegu kilku linii : dwie są krajowe , a mianowicie Neapol – Salerno i Salerno – Reggio Calabria i dwie regionalne , mianowicie Salerno – za pośrednictwem Benevento Salerno i Avellino – Caserta .
Istnieje też stara linia kolejowa Salerno – LaGrange poprzez Cava de ’ Tirreni , która była używana przed budową tunelu w Saint Lucia , teraz obsługiwana przez pociągi regionalne .
