Al-Karak
Al-Karak ( albo `` Karak '' lub `` Kerak '' ) − miasto w Jordanii , stolica muhafazy Al-Karak .
Historia .
Al-Karak utożsamiany jest z biblijnym Kir-Chareszet , stolicą Moabu , otoczoną murem twierdzą , położoną na szczycie górskim .
Według 2 Księgi Królewskiej ( 2 Krl 3 , 1,25–26 ) , król Judy – Jozafat oraz Joram , po objęciu władzy w Izraelu , podjęli nieudaną próbę zdobycia Kir-Chareszet .
Biblia przekazuje różne formy nazwy miasta : Kir-Moab – Księga Izajasza ( Iz 15,1 ) , Kir-Chareset – Księga Izajasza ( Iz 16,7 ) , Kir-Chares – Księga Izajasza ( Iz 16,11 ) , Kir-Cheres – Księga Jeremiasza ( Jr 48,31 ) .
W epoce rzymskiej i bizantyjskiej miasto jako Characmoba wchodziło w skład prowincji Arabia .
Wnioskując z odcisków miejskich pieczęci , znalezionych w Mampsis , w II wieku Kir-Chareszet był stolicą okręgu .
Lokalizacja miasta została wskazana na mapie z Medeby .
