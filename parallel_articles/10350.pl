Salford
Salford – miasto w dystrykcie metropolitalnym Salford w Wielkiej Brytanii , w Anglii , w hrabstwie metropolitalnym Wielki Manchester , nad rzeką Irwell ( dopływ Mersey ) i Kanałem Manchesterskim .
W 2001 liczyło 72 750 mieszkańców .
Miejscowość została wymieniona w Domesday Book ( 1086 ) , jako `` Salford '' .
Stanowi siedzibę Uniwersytetu Salfordzkiego oraz katolickiej diecezji Salford .
Jest miejscem narodzin i śmierci fizyka Jamesa Joule ' a i dziennikarza Tony'ego Wilsona oraz aktora Roberta Powella .
W Salford urodził się również Paul Scholes – piłkarz reprezentacji Anglii oraz klubu Manchester United F.C .
W mieście znajduje się stacja kolejowa Salford Central .
W mieście rozwinął się przemysł włókienniczy , chemiczny , elektroniczny oraz maszynowy .
