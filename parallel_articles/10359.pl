Salt Lake City
Salt Lake City – miasto w zachodniej części Stanów Zjednoczonych i stolica stanu Utah , położone nad rzeką Jordan , na wschód od Wielkiego Jeziora Słonego .
Miasto jest siedzibą światowych władz i świątyni Kościoła Jezusa Chrystusa Świętych w Dniach Ostatnich .
Gospodarka .
Salt Lake City jest dużym ośrodkiem gospodarczym .
Rozwinięty przemysł metalurgiczny , maszynowy , chemiczny i spożywczy .
W mieście swoje siedziby i oddziały ma wiele banków , towarzystw ubezpieczeniowych oraz spółek wydawniczych .
Miasto jest także ważnym węzłem komunikacyjnym .
Przez Salt Lake City przebiegają drogi międzystanowe nr 15 ( biegnąca od granicy z Kanadą do Kalifornii ) oraz nr 80 ( łącząca San Francisco z Bostonem ) .
Międzynarodowy port lotniczy w Salt Lake City w 2007 roku obsłużył ponad 22 mln pasażerów .
Przez miasto przebiegają także linie kolejowe .
W 2002 r. miasto było gospodarzem Zimowych Igrzysk Olimpijskich .
Zabytki .
Główne obiekty zabytkowe związane są bezpośrednio z historią i kulturą mormonów pochodzące z XIX wieku .
W mieście znajduje się świątynia , budowana w latach 1853-1893 , Mormon Tabernacle Choir , Museum of Church History and Art a także kilka innych muzeów .
Struktura wyznaniowa .
Salt Lake City jest znane z bardzo dużej społeczności mormonów - około 70 % mieszkańców deklaruje się jako członkowie Kościoła Jezusa Chrystusa Świętych w Dniach Ostatnich .
Tworzy to specyficzną kulturę w tym mieście : wydawane są mormońskie gazety , książki , broszury .
Wiele szkół , placówek edukacyjnych , szpitali i innych obiektów użyteczności publicznej należy bezpośrednio do Kościoła lub jest przez niego wspomagana .
Organizowane są liczne konferencje i spotkania wiernych Kościoła .
Pozostałe wyznania to protestantyzm , katolicyzm i judaizm .
Około 10 % ludności miasta nie identyfikuje się z żadną religią .
Sport .
W Salt Lake City grają drużyny Utah Jazz ( NBA ) i Real Salt Lake ( MLS ) .
W 2002 roku miasto zorganizowało Zimowe Igrzyska Olimpijskie .
W mieście odbywają się także zawody skoków narciarskich .
W Salt Lake City mieszkał polski łyżwiarz szybki Stanisław Kłotkowski .
