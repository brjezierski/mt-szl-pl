Al-Mahalla al-Kubra
Al-Mahalla al-Kubra ( , `` Al-Maḩallah al-Kubrá '' ) – miasto w północnym Egipcie , położone w delcie Nilu .
Największe miasto Prowincji Zachodniej i drugie co do wielkości miasto Delty Nilu o liczbie ludności 535 278 osób ( według stanu na 2012 ) .
Historia najnowsza .
Protesty z lat 2006–11 .
Ponad 15 000 pracowników starło się z policją w 2006 po opublikowaniu w Danii rysunków ośmieszających islam .
Później w tym samym roku pracownicy zakładów tekstylnych strajkowali , by zaprotestować przeciw reformom rynkowym , żądając poprawy warunków życia
Na początku kwietnia 2008 w mieście odbyły się masowe demonstracje przeciwko wynikom wyborów , w których wygrał prezydent Mubarak .
Oskarżano o oszustwo wyborcze i żądano lepszych płac .
