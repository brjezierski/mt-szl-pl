Salto ( departament )
Salto – urugwajski departament położony w północno-zachodniej części kraju .
Rzeka Urugwaj stanowi zachodnią granicę z argentyńską prowincją Entre Ríos .
Ponadto Salto graniczy z następującymi departamentami : od północy z Artigas , na północnym wschodzie z Rivera , na wschodzie z Tacuarembó , a od południa z Paysandú .
Ośrodkiem administracyjnym oraz największym miastem tego powstałego w 1837 r. departamentu jest Salto ( drugie co do wielkości miasto w kraju ) .
Powierzchnia Salto wynosi 14 163 km² .
W 2004 r. departament zamieszkiwało 123 120 osób , co dawało gęstość zaludnienia 8,7 mieszk./km² .
