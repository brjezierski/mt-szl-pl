Salwador ( film )
Salvador – amerykański film wojenny z 1986 roku oparty na faktach .
Fabuła .
Rok 1980 .
Richard Boyle , fotoreporter i łowca sensacji wraz z ekscentrycznym DJ-em wyrusza do Salwadoru .
Odszukuje swą byłą dziewczynę Marię .
Postanawia wywieźć ją do USA .
Ale Richard jest świadkiem wydarzeń w tym kraju : wojskowa dyktatura , zamach na arcybiskupa Oscara Romero .
Postanawia przy okazji zrobić mocny reportaż o tym , co się dzieje w Salwadorze .
Nagrody i nominacje .
Oscary za rok 1986
