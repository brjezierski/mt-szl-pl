Saloniki
Saloniki ( , `` Tesaloniki '' ) , hist .
`` Tesalonika '' ( , `` Thessaloníkē '' , , , w kontekście ) – miasto w północnej Grecji , nad Zatoką Salonicką ( Morze Egejskie ) .
Drugie co do wielkości ( po Atenach ) miasto Grecji .
Siedziba administracji zdecentralizowanej Macedonia-Tracja , regionu Macedonia Środkowa , jednostki regionalnej Saloniki i gminy Saloniki .
W 2011 Saloniki liczyły 315 196 mieszkańców , a cały zespół miejski około 790 tys .
