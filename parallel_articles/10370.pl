Etymologia .
Nazwa miasta pochodzi prawdopodobnie od słów w języku sogdyjskim – `` asmara '' , czyli „ kamień , skała ” , oraz `` kand '' oznaczającego „ fort , gród ( miasto ) ” .
Historia .
Samarkanda jest jednym z najdłużej zamieszkanych miast świata .
Starożytne miasto pod nazwą Marakanda powstało w VI wieku p.n.e .
jako stolica satrapii sogdyjskiej pod panowaniem perskiej dynastii Achemenidów .
W 329 r. p.n.e .
miasto zostało zdobyte przez Aleksandra Wielkiego .
Okres rozwoju nastąpił dzięki powstaniu szlaku jedwabnego , łączącego Europę i Chiny .
Samarkanda stała się największym miastem Azji Środkowej .
W 712 roku została podbita przez Arabów , którzy wprowadzili religię islamską .
