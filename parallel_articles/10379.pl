Samuel Fränkel
Samuel Fränkel ( ; ur .
22 listopada 1801 w Białej , zm .
28 lipca 1881 w Prudniku ) – niemiecki przemysłowiec , założyciel tkalni lnu i adamaszku ( późniejszy „ Frotex ” ) w Prudniku .
Życiorys .
Rodzina .
Pochodził z żydowskiej rodziny kupieckiej .
Był synem Abrahama Isaaka Fränkla ( 1750–1835 ) i jego drugiej żony Chandel Fränkel ( 1773–1866 ) .
W 1824 Samuel poślubił Esterę Polke ( 1802–1879 ) , córkę Löbela Polkego i Berachy Adler .
W małżeństwie tym urodziło się czternaścioro dzieci ( jedenaścioro przeżyło wiek dziecięcy ) : Zwi , Menachem , Jehuda ( 1825–1858 ) , Akiba ( 1826–1834 ) , Dorothea ( 1829–1904 ) , Cäcilie ( 1830–1916 ) , Josef ( 1831–1864 ) Jenny ( 1833–1892 ) , Abraham ( 1835–1904 ) , Albert ( 1837–1909 ) , Auguste ( 1838–1919 ) , Rosalie ( 1840–1913 ) , Emanuel ( 1842–1920 ) i Hermann ( 1844–1901 ) .
Kariera .
W roku 1827 Samuel Fränkel wraz z rodziną przeniósł się z Białej do Prudnika .
Po zamieszkaniu w Prudniku założył sklep w wynajmowanej kamienicy na Rynku , w którym sprzedawał między innymi tkaniny wytwarzane w lokalnych warsztatach , materiały lniane , parasole , porcelanę i zabawki .
Dopiero w 1839 kupił budynek od Barbary Pulzner za 3090 talarów .
W 1845 roku Fränkel założył fabrykę tkanin lnianych tuż nad brzegiem rzeki Prudnik przy ulicy Nyskiej ( daw .
`` Neisserstrasse '' ) .
W 1847 przejął konkurencyjną , bankrutującą fabrykę Augusta Thilla .
Zakład Fränkla bardzo szybko stał się jednym z największych zakładów tekstylnych nie tylko na Śląsku i w Niemczech , ale również w Europie .
Otworzył kilka filii ( między innymi w Berlinie , Augsburgu , Głuchołazach , Kietrzu i Korfantowie ) , by w końcu stać się jednym z największych producentów lnu na świecie .
Został przewodniczącym prudnickiej gminy żydowskiej po jej utworzeniu w 1854 .
W grudniu 1864 roku zmieniono formę organizacyjną firmy Fränkla , która odtąd aż do 1934 widniała w rejestrze handlowym jako `` Die Offene Handelsgesellschaft S. Fränkel '' ( Otwarta Spółka Handlowa S. Fränkel ) .
Tworzyli ją Samuel Fränkel , jego syn Albert oraz mąż jego córki Augusty – Josef Pinkus .
3 maja 1877 Fränkel dokonał poświęcenia ufundowanej przez siebie synagogi w Prudniku ( spalona w 1938 podczas nocy kryształowej ) .
Często organizował odczyty poezji oraz kameralne koncerty znanych na świecie pianistów ( m.in .
Wilhelma Backhausa i Waltera Giesekinga ) .
Śmierć .
Fränkel zmarł 28 lipca 1881 .
Został pochowany na cmentarzu żydowskim w Prudniku przy ul .
Kolejowej .
Na mocy jego testamentu przekazano 900 marek prudnickim szpitalom , 2100 klasztorowi bonifratrów na zakup nowych łóżek do przyklasztornej lecznicy i 15000 marek prudnickiemu magistratowi , z czego co roku 11 listopada i 11 grudnia wydawano na węgiel , który rozdawano potrzebującym mieszkańcom miasta .
Właścicielem fabryki został jego zięć Josef Pinkus , a następnie jego wnuk Max Pinkus .
Przedsiębiorstwo istniało do 2011 jako Zakłady Przemysłu Bawełnianego „ Frotex ” .
Upamiętnienie .
28 listopada 1901 w prudnickiej synagodze został odśpiewany „ Hymn z okazji 100. urodzin niezapomnianego , tajnego radcy pana Samuela Fränkla ” .
Został zagrany ponownie 5 grudnia 2019 przez zespół skrzypcowy uczniów Państwowej Szkoły Muzycznej I stopnia im .
Karola Szymanowskiego w Prudniku w ramach promocji polsko-czeskich publikacji Marcina Husaka pt .
„ Fränklowie i Pinkusowie – ocalałe dziedzictwo ” oraz Michala Vychlidala pt .
„ Flemmichowie i Schielowie – tekstylne dziedzictwo ” .
18 czerwca 2016 jego imieniem nazwano platan klonolistny znajdujący się przy dawnej portierni „ Frotexu ” .
28 maja 2020 z inicjatywy burmistrza Prudnika Grzegorza Zawiślaka imię Samuela Fränkla otrzymała nowo powstała ulica łącząca Jasionowe Wzgórze z ul .
