Al-Ubajjid
Al-Ubajjid ( arab .
الأبيض , `` Al-‛Ubayyiḑ '' ) – miasto w środkowym Sudanie , stolica stanu Kordofan Północny , liczy ok. 229,5 tys .
mieszkańców ( stan z roku 1993 ) .
Transport .
Al-Ubajjid jest ważnym węzłem komunikacyjnym - krzyżują się trasy karawan .
Przez miasto przebiega również szlak pielgrzymów wiodący z Nigerii do Mekki .
Obecnie znajduje się tu również lotnisko międzynarodowe i rafineria ropy naftowej .
Miasto jest stacją końcową linii kolejowej kolei sudańskich Chartum-Kusti-Al-Ubajjid .
