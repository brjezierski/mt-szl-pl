Samuel Umtiti
Samuel Umtiti ( ur .
14 listopada 1993 w Jaunde ) – francuski piłkarz kameruńskiego pochodzenia występujący na pozycji obrońcy we włoskim klubie US Lecce , do którego jest wypożyczony z FC Barcelony .
Kariera klubowa .
Umtiti rozpoczął treningi piłki nożnej w 1999 roku w FC Menival .
W 2001 roku trafił do Olympique Lyon , a w 2012 został zawodnikiem pierwszej drużyny .
W lipcu 2016 roku piłkarz podpisał 5-letni kontrakt z FC Barceloną .
Kataloński klub zapłacił za niego 25 milionów euro .
Lewonożny środkowy obrońca już w pierwszym sezonie w klubie stał się ważną postacią i rozegrał 43 mecze .
W szczytowym okresie kariery , który przypadł na 2018 rok jego wartość rynkowa szacowana była na 70 milionów euro .
Ze względu na przewlekłą kontuzję kolana jego wartość rynkowa w 2021 spadła do 2 mln euro .
Kariera reprezentacyjna .
Karierę reprezentacyjną Umtiti rozpoczął od występów w reprezentacji Francji U-17 .
W latach 2010–2011 grał w reprezentacji U-18 .
W latach 2011–2012 grał w reprezentacji U-19 .
W latach 2012–2013 był zawodnikiem kadry do lat 20 , z którą został mistrzem świata U-20 w 2013 roku .
W dorosłej reprezentacji zadebiutował 3 lipca 2016 w wygranym 5:2 meczu z Islandią w ćwierćfinale Euro 2016 .
W 2018 sięgnął z Francją po mistrzostwo świata .
