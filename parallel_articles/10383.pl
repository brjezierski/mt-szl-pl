San Bernardino ( Suchitepéquez )
San Bernardino – miejscowość na południowym zachodzie Gwatemali , w departamencie Suchitepéquez .
Według danych szacunkowych z 2012 roku liczba mieszkańców wynosiła 9 664 osób .
San Bernardino leży około 6 km na wschód od stolicy departamentu – miasta Mazatenango .
Miejscowość leży na wysokości 383 metrów nad poziomem morza , u podnóża gór Sierra Madre de Chiapas , w odległości około 45 km od brzegu Pacyfiku .
Gmina San Bernardino .
Miejscowość jest także siedzibą władz gminy o tej samej nazwie , która jest jedną z dwudziestu gmin w departamencie .
W 2013 roku gmina liczyła 19 978 mieszkańców .
Gmina jak na warunki Gwatemali jest mała , a jej powierzchnia obejmuje 32 km² .
Jest zamieszkała głównie przez ludność posługującą się językiem kicze .
Mieszkańcy gminy utrzymują się głównie z uprawy roli , rzemiosła artystycznego i turystyki ze względu na sąsiedztwo ruin z okresu prekolumbijskiego .
Klimat gminy jest równikowy , według klasyfikacji Köppena , należy do klimatów tropikalnych monsunowych ( `` Am '' ) , z wyraźną porą deszczową występującą od maja do października .
