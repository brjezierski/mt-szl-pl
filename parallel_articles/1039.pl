Alabama ( rzeka )
Alabama ( ) – rzeka w Stanach Zjednoczonych , w stanie Alabama , o długości 512 km .
Powstaje około 11 km na północ od miasta Montgomery z połączenia rzek Coosa i Tallapoosa , które biorą swój początek w Appalachachy .
71 km na północ od Mobile łączy się z rzeką Tombigbee , dając początek rzece Mobile ( ta uchodzi do zatoki Mobile , będącej częścią Zatoki Meksykańskiej ) .
Żeglowna od Montgomery , uregulowana , jest ważną arterią komunikacyjną .
