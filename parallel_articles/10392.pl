San Fernando ( Argentyna )
San Fernando – miasto w Argentynie , położone w północnej części prowincji Buenos Aires .
Opis .
Miejscowość została założona w 1806 roku .
W odległości 26 km od miasta , znajduje się aglomeracja i stolica kraju Buenos Aires .
W mieście znajduje się węzeł drogowy-RP195 i RP202 i linia kolejowa .
Gospodarka .
W mieście rozwinął się przemysł mleczarski , rybny oraz papierniczy .
