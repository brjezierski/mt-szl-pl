San Leandro
San Leandro – miasto w Stanach Zjednoczonych , w stanie Kalifornia , w hrabstwie Alameda .
Według spisu ludności z roku 2010 , w San Leandro mieszka 84 950 mieszkańców .
W mieście rozwinął się przemysł maszynowy , samochodowy , lotniczy oraz metalowy .
W San Leandro urodził się Lloyd Bridges , amerykański aktor .
