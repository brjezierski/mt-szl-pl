Alajuela ( jezioro )
Alajuela – sztuczne jezioro w dorzeczu rzeki Chagres , w Panamie .
Jezioro jest ograniczone przez Zaporę Madden i połączone z Kanałem Panamskim .
Jezioro Alajuela służy jako zbiornik dla Kanału Panamskiego , który leży na południowy zachód od jeziora .
Do jeziora wpływają rzeki : Chagres , Pequení , Boquerón , Salamanca , La Puente , Indio , Piedras , San Cristóbal i Escandaloso .
Rzeki te charakteryzują się tym , że są stosunkowo krótkie , ze stromymi zboczami i niewielką pojemnością w swoich dolinach .
Zlewnia jeziora Alajuela wynosi 1026 km² .
Na początku grudnia 2010 r. Jezioro Alajuela osiągnęło najwyższy odnotowany poziom wody , co skłoniło władze do zamknięcia Kanału Panamskiego na 17 godzin .
Kanał ponownie otwarto 9 grudnia .
Nad jeziorem lub w pobliżu znajdują się takie miejscowości jak : Nuevo Vigía , Nuevo Caimitillo , Caimitillo , Calzada Larga i Chilibre .
