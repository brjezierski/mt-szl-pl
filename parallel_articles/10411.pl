San Luis Potosí ( miasto )
San Luis Potosí – miasto w środkowym Meksyku , na obszarze Mesy Centralnej ( Wyżyna Meksykańska ) , na wysokości około 1880 metrów .
Jest stolicą stanu o tej samej nazwie .
W 2014 roku miasto liczyło około 761,7 tysięcy mieszkańców .
San Luis Potosí zostało założone w 1576 roku jako ośrodek górniczy .
W 1658 roku otrzymało prawa miejskie .
