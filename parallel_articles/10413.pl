San Marino
San Marino , Republika San Marino ( ) , również znana jako Najjaśniejsza Republika San Marino ( ) – państwo śródlądowe w Europie Południowej stanowiące enklawę na obszarze Włoch .
Stolicą kraju jest miasto San Marino .
Historia .
do XV wieku .
Tradycyjnie za symboliczny początek współcześnie istniejącej państwowości uznawane jest założenie przez świętego Maryna w roku 301 n.e .
