San Miguel de Tucumán
San Miguel de Tucumán ( w użyciu również skrócona nazwa Tucumán ) – największe miasto północno-zachodniej Argentyny , liczy 525 823 mieszkańców ( 2001 ) , zespół miejski 738 000 ( piąty co do wielkości w Argentynie ) .
Miasto jest stolicą prowincji Tucumán .
Położone jest nad rzeką Dulce .
Historia .
Miasto zostało założone w 1565 , a w 1685 przeniesione w obecne miejsce .
