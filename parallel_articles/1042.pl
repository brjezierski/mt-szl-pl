Alak
Alak ( arab .
آلاك , fr .
`` Aleg '' ) – miasto w południowej Mauretanii , stolica regionu Al-Barakina .
Alak leży ok. 250 kilometrów na południowy wschód od Nawakszutu przy `` Route de l ’ Espoir '' ( „ Drodze Nadziei ” ) – trasie komunikacyjnej łączącej Nawakszut z An-Namą .
Miasto jest ważnym węzłem komunikacyjnym , położonym w połowie drogi między Kifą – jednym z większych miast na południu Mauretanii – a stolicą kraju Nawakszutem .
Dodatkowo drogi odchodzą stąd na południe w kierunku Kajhajdi .
