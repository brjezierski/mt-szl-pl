San Pedro de Macorís ( miasto )
San Pedro de Macorís – miasto w południowo-wschodniej części Dominikany , nad Morzem Karaibskim , 64 km na wschód od Santo Domingo .
Ludność : 193,7 tys .
( 2002 ) – trzecie pod względem liczby ludności miasto kraju .
Ośrodek administracyjny prowincji San Pedro de Macorís .
Dawne nazwy : Sultana del Este , Macorís del Mar , La Tasita de Oro .
San Pedro de Macorís to ważny port morski i rybacki Dominikany .
Stanowi główny port wywozu cukru .
Jest to ważny ośrodek handlowy i przemysłowy tej części kraju .
