Sandusky ( Ohio )
Sandusky – miasto w Stanach Zjednoczonych , w północnej części stanu Ohio , położone nad jeziorem Erie .
Liczba mieszkańców w 2000 roku wynosiła 27 968 .
Miasto słynie z parku rozrywki Cedar Point , jednego z największych tego typu na świecie .
W mieście rozwinął się przemysł maszynowy , metalowy oraz papierniczy .
Port .
Sandusky ma port promowy oraz przeładunkowy .
Ponadto na wybrzeżu jeziora Erie znajdują się trzy mariny : Venetian , Sandusky Harbor Marina , Dock and Bay Marina .
Nazwa .
Nazwa miejscowości wywodzi się od nazwiska założyciela Antoniego Sadowskiego polskiego szlachcica , tłumacza oraz pioniera dzikiego zachodu .
Klimat .
Miasto leży w strefie klimatu kontynentalnego , z gorącym latem , należącego według klasyfikacji Köppena do strefy `` Dfa '' .
Średnia temperatura roczna wynosi 10,8°C , a opady 843,3 mm ( w tym do 41,1 cm opadów śniegu ) .
Średnia temperatura najcieplejszego miesiąca - lipca wynosi 23,5°C , najzimniejszego - stycznia -2,7°C , podczas gdy rekordowe temperatury wynoszą odpowiednio 40,6°C i -26,7°C .
