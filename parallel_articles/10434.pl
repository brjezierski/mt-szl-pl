Sandżak
Sandżak ( , ) – region geograficzno-historyczny na pograniczu Serbii i Czarnogóry .
Głównym miastem jest Novi Pazar .
Region jest zamieszkany przez liczną mniejszość boszniacką .
Sandżak to również nazwa potoczna dzisiejszego okręgu w Serbii – `` Raszka '' .
Etymologia nazwy .
Nazwa krainy Sandżak pochodzi od tureckiego słowa Sancak będącego określeniem prowincji Imperium Osmańskiego .
W dosłownym tłumaczeniu nazwa ta oznacza sztandar .
Przed Turkami w Europie region ten nosił nazwę Raszka , która w serbskiej części używana jest również dzisiaj .
Obecnie nazwa Sandżak nie jest oficjalną nazwą żadnej jednostki administracyjnej ani w Serbii ani w Czarnogórze .
Historia .
Na terytorium Raszki powstało pierwsze państwo serbskie , utworzony w VIII wieku gród Ras , który był stolicą Serbii do połowy XIII wieku .
