St. Pölten
St. Pölten , Sankt Pölten ( ) – statutarne miasto powiatowe w północno-wschodniej Austrii , stolica kraju związkowego Dolna Austria oraz siedziba powiatu St. Pölten-Land , do którego jednak nie należy .
Leży nad rzeką Traisen ( dopływ Dunaju ) , ok. 60 km na zachód od Wiednia .
Liczy 52,15 tys .
mieszkańców ( 1 stycznia 2014 ) .
Miasto zdobią barokowe budowle będące dziełem mieszkającego tutaj architekta Jakoba Prandtauera .
Nazwa miasta pochodzi od austriackiej formy imienia Hipolit .
Patronem miasta był dlatego pierwszy antypapież w historii Hipolit Rzymski .
