Sankt Vith
Sankt Vith ( fr .
`` Saint‐Vith '' ) – miasteczko w południowo-wschodniej Belgii w Prowincji Liège .
Historia .
W dniach 17–23 grudnia 1944 St. Vith stało się teatrem walk pomiędzy wojskami amerykańskimi a niemieckimi .
Niemcy , posuwając się naprzód w swej ofensywie , dotarli do miasta 17 grudnia .
Opanowali je dopiero 20 , a pobliskie skrzyżowanie , na które wycofali się Amerykanie , 23 .
To opóźnienie pozwoliło Brytyjczykom pod Dinant umocnić się i ostatecznie odeprzeć ofensywę .
