Sanok ( stacja kolejowa )
Sanok – stacja kolejowa w Sanoku , w województwie podkarpackim , w Polsce .
W roku 2017 stacja obsługiwała 10–19 pasażerów na dobę .
Historia .
Budynek został zbudowany przez władze C. K. Austro-Węgier w 1884 roku w związku z uruchomieniem nowej linii kolejowej w ramach Kolei Transwersalnej w tym samym roku .
Pierwszy pociąg wjechał na stację latem 1884 roku .
Stanowisko naczelnika stacji Sanok pełnili : Wilhelm Stehlik ( od 1884 , od około 1889 w charakterze adjunkta , od około 1893 do około 1894 w charakterze oficjała ) , Juliusz Stampf ’ l ( od około 1894 ) , inż .
Feliks Wodziczko ( od około 1898 ) , Józef Trzciński ( od około 1900 w charakterze komisarza budownictwa , od około 1906 starszego komisarza budownictwa , od około 1913 w randze inspektora ) .
W 1913 została ustanowiona ulica Kolejowa biegnąc do dworca kolejowego .
W 1917 przez stację przebiegały szlaki trzech tras pociągów : Stróże - Nowy Zagórz ( 3-krotnie ) , Iwonicz - Sanok ( 1 ) , Sanok - Chyrów ( 1 ) .
Po zakończeniu I wojny światowej i odzyskaniu przez Polskę niepodległości od 19 lutego 1919 funkcję komendanta dworca kolejowego Sanok pełnił ppor .
