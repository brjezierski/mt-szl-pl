Santa Barbara ( serial telewizyjny )
Santa Barbara – amerykański serial telewizyjny z gatunku opera mydlana emitowany od 30 lipca 1984 do 15 stycznia 1993 przez stację telewizyjną NBC .
Serial rozgrywa się w kalifornijskim mieście Santa Barbara i opowiada o losach bogatej rodziny Capwellów i konkurującej z nią rodziny Lockridge'ów .
Emitowany był od poniedziałku do piątku i składa się z 2137 odcinków .
W Polsce `` Santa Barbara '' wyświetlany był przez Program Drugi TVP ( dzisiejszą TVP2 ) od 2 lipca 1990 do lipca 1991 .
Wyemitowano 149 odcinków .
Wznawiany był przez stacje ATV oraz Dla Ciebie .
Podczas emisji w TVP zastosowano nietypowy zabieg ; listę dialogową czytało dwoje lektorów – kobieta ( początkowo Jolanta Niewiadomska , później Danuta Stachyra – role żeńskie ) , a mężczyzna ( Władysław Frączak , a następnie Tomasz Knapik ) – męskie .
