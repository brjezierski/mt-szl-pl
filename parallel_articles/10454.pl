Santa Cruz do Sul
Santa Cruz do Sul – miasto położone w centralnej części stanu Rio Grande do Sul w południowej Brazylii .
Liczy w przybliżeniu 117 000 mieszkańców i położone jest około 150 km od głównego miasta stanu , Porto Alegre .
PKB jest przeciętnie 2,5 raza wyższy niż w stanie Rio Grande do Sul .
W mieście działa duża wytwórnia papierosów marki Philip Morris .
W mieście rozwinął się przemysł chemiczny oraz tytoniowy .
Głównym obiektem turystycznym jest katedra São João Batista z 1928 roku – jedna z największych katedr w Brazylii .
Dwie wieże katedralne , dobudowane w 1977 roku , liczą po 82 metry .
W mieście znajduje się uniwersytet założony w 1962 roku ( `` Universidade de Santa Cruz do Sul '' ) .
Historia .
Rok 1877 to oficjalna data założenia miasta przez niemieckich przybyszów .
W roku 1924 wybudowany został przez nich kościół ewangelicki , który jest największym kościołem ewangelicko-luterańskim w Rio Grande do Sul i największym neoromańskim kościołem w Ameryce Łacińskiej .
Miasto posiada dużą populację osób pochodzenia niemieckiego , które przyczyniły się do powstania tutejszego corocznego festiwalu Oktoberfest , będącego kopią festiwalu w Bawarii .
