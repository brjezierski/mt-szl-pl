Historia .
Sprzedaż Alaski .
W drugiej połowie XIX w. należące do Rosji tereny Alaski zostały odsprzedane Stanom Zjednoczonym .
Traktat został podpisany po długich negocjacjach w nocy 29/30 marca 1867 .
Transakcję wartą 7,2 mln dolarów w imieniu Rosji zawarł baron Edward de Stoeckl , a w imieniu Stanów Zjednoczonych William Seward , który wcześniej zabiegał o zakup tych terenów .
Traktat został ratyfikowany przez Senat Stanów Zjednoczonych 9 kwietnia 1867 , Izba Reprezentantów 14 lipca 1868 uchwaliła wyasygnowanie pieniędzy .
System polityczny .
Władzę ustawodawczą na szczeblu stanowym sprawuje dwuizbowa Legislatura Alaski , złożona z Izby Reprezentantów i Senatu .
Na czele władzy wykonawczej stoi gubernator , wybierany w wyborach bezpośrednich na czteroletnią kadencję .
