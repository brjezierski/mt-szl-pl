Alaska
Alaska ( ) – stan Stanów Zjednoczonych w północno-zachodniej części Ameryki Północnej , będący eksklawą Stanów Zjednoczonych .
Graniczy od wschodu z Kanadą , a od zachodu , przez Cieśninę Beringa , z Rosją .
Dominują góry , z najwyższym punktem Ameryki Północnej , jakim jest szczyt Denali ( McKinley , 6194 m n.p.m .
) , oraz wyżyny i liczne stożki wulkaniczne .
Do terytorium należą też Aleuty .
Alaska ma największą powierzchnię wśród stanów Stanów Zjednoczonych oraz najmniejszą gęstość zaludnienia .
W odróżnieniu od większości stanów Alaska nie dzieli się na hrabstwa , lecz okręgi ( `` borough '' ) , znaczna część stanu jest przy tym niezorganizowana .
Geografia .
Powierzchnia stanu jest górzysta .
Na południu znajdują się góry Alaska z najwyższym szczytem Ameryki Północnej – Denali ( w latach 1896–2015 : McKinley , 6194 m ) , Góry Świętego Eliasza ( Góra Świętego Eliasza , 5489 m ) , Góry Wrangla ( Mount Bona , 5044 m ) , na północy Góry Brooksa ( Michelson , 2816 m ) .
W środkowej części stanu płaskowyż jest rozcięty dolinami rzek , natomiast wzdłuż wybrzeża występują niziny .
Linia brzegowa jest bardzo dobrze rozwinięta ; składają się na nią liczne półwyspy ( Alaska , Kenai ) i przybrzeżne wyspy ( Aleuty , Archipelag Aleksandra , Kodiak , Nunivak , Wyspa Świętego Wawrzyńca , Wyspy Pribyłowa ) .
Obszar ten jest aktywny sejsmicznie , a wiele szczytów gór to czynne ( m.in .
Pavlof , Iliamna ) i wygasłe wulkany .
Klimat .
W części południowej klimat jest umiarkowany chłodny , a na dalekiej północy subpolarny .
Zakres zmian temperatury powietrza w ciągu roku : od w zimie do w lecie .
Zima trwa 9 miesięcy i jest bardzo mroźna , a w czasie jej trwania wieją silne wiatry , osiągające prędkość 100 km/h oraz powstają burze śnieżne charakterystyczne dla strefy tundry .
Roślinność .
W głębi lądu tajga i tundra , w zasięgu oddziaływania Prądu Alaskańskiego iglaste lasy deszczowe strefy umiarkowanej z topolami , olchami i wierzbami .
