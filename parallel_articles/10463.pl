Santa Rosa ( Ekwador )
Santa Rosa – miasto w Ekwadorze , w prowincji El Oro , stolica kantonu Santa Rosa .
Opis .
Miasto zostało założone w 1600 roku .
Przez miasto przebiega droga krajowa E25 , E584A i E586 .
W mieście znajduje się krajowy port lotniczy .. Santa Rosa wchodzi w skład obszar metropolitalnego miasta Machala .
