Santander ( departament )
Santander – departament Kolumbii .
Był niegdyś jednym z dziewięciu stanów , tworzących Stany Zjednoczone Kolumbii .
Leży w środkowo-północnej części kraju .
Jego granice stanowią : rzeka Magdalena od zachodu , od południa i wschodu departament Boyacá , a od północy Norte de Santander .
Stolicą departamentu Santander jest miasto Bucaramanga .
