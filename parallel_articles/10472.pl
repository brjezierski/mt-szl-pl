Santiago del Estero ( prowincja )
Santiago del Estero – argentyńska prowincja położona w północnej części kraju .
Zajmuje obszar 136 tys .
km² , liczba mieszkańców w 2001 roku wynosiła 804 tys .
Stolicą prowincji jest miasto Santiago del Estero .
1 stycznia 2011 doszło w prowincji do silnego trzęsienia ziemi .
Wstrząsy miały siłę 6,9 stopnia w skali Richtera .
