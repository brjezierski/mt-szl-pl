Sapporo
– piąte co do wielkości miasto Japonii , największe miasto wyspy Hokkaido i stolica tejże prefektury .
Ośrodek sportów zimowych , gospodarz Festiwalu Śniegu .
W mieście znajdują się 4 uniwersytety publiczne , kilkanaście prywatnych uczelni wyższych , muzea i chramy shintō .
Miasto ma powierzchnię 1 121,26 km² .
W 2020 r. mieszkało w nim 1 975 065 osób , w 966 009 gospodarstwach domowych ( w 2010 r. 1 914 434 osoby , w 886 338 gospodarstwach domowych ) .
Nazwa miasta pochodzi z języka ajnuskiego , od `` sat poro pet '' – „ wielka sucha rzeka ” .
Historia .
