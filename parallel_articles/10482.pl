Sapucaia do Sul
Sapucaia do Sul – miasto w południowej Brazylii , w stanie Rio Grande do Sul .
W 2007 roku liczba mieszkańców wynosiła 141 808 .
Zajmuje powierzchnię 58,247 km² .
Prawa miejskie miasto otrzymało 20 sierpnia 1961 roku .
W mieście rozwinął się przemysł metalurgiczny , spożywczy , włókienniczy , chłodniczy oraz skórzany .
