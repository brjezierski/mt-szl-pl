Sarah Bernhardt
Sarah Bernhardt , właśc .
Henriette Rosine Bernardt ( ur .
22 października 1844 w Paryżu , zm .
26 marca 1923 tamże ) – francuska aktorka .
Występowała głównie w teatrach Paryża , w latach 1872–1880 w Comédie-Française .
Była znana z teatralnych ról dramatycznych .
Życiorys .
Urodziła się w rodzinie żydowskiej jako Rosine Bernardt .
Jej matką była pochodząca z Amsterdamu Julie Bernardt ( 1821–1876 ) , córka kupca Moritza Barucha Bernardta i jego żony Sary Hirsch .
Aktorka zmieniła rodzinne nazwisko , modyfikując je w formę `` Bernhardt '' .
Sceniczna kariera Bernhardt zaczęła się w 1862 , kiedy młoda aktorka studiowała jeszcze w Comédie-Française , najbardziej prestiżowym teatrze we Francji .
W pewnym momencie zdecydowała się opuścić rodzinny kraj i przeniosła się do Belgii , gdzie została kochanką arystokraty Henriego de Ligne .
W 1864 urodził się im syn Maurice .
Po narodzinach dziecka książę zaproponował jej małżeństwo , ale jego rodzina sprzeciwiła się legalizacji związku i przekonała Bernhardt do odrzucenia oświadczyn i zakończenia związku .
Po wyrzuceniu z Comédie-Française za spoliczkowanie jednej z aktorek powróciła do życia kurtyzany , z którym zapoznała ją jej matka w bardzo młodym wieku .
Dzięki temu w latach 1862–1865 zarobiła pokaźną sumę pieniędzy .
W tym czasie nabyła swoją słynną trumnę , która często służyła jej za łóżko .
