Sarajewo
Sarajewo ( serb.-chorw .
`` Sarajevo '' , `` Сарајево '' IPA : [ sǎrajɛʋɔ ] , tur .
`` Saraybosna '' ) – stolica Bośni i Hercegowiny zamieszkana przez 395 tys .
osób ( 2013 ) .
Założone w 1462 r. przez Turków Osmańskich .
W 1914 r. miejsce zabójstwa arcyksięcia Franciszka Ferdynanda .
Odbyły się tu Zimowe Igrzyska Olimpijskie 1984 .
Silnie zniszczone w rezultacie działań wojennych 1992-1995 .
Sarajewo sąsiaduje od wschodu z Sarajewem Wschodnim ( ok. 60 tys .
mieszkańców ) , formalną stolicą Republiki Serbskiej .
Geografia .
Sarajewo leży w centralnym punkcie Bośni i Hercegowiny .
Zajmuje powierzchnię 142 km² .
Przepływa przez nie rzeka Miljacka .
W pobliżu dzielnicy Ilidża znajduje się malownicze źródło rzeki Bośnia ( Vrelo Bosne ) .
Miasto położone jest na wysokości 500 m n.p.m. , w kotlinie pomiędzy pięcioma górami pasma Alp Dynarskich : Bjelašnica ( 2067 m ) , Igman ( 1502 m ) , Jahorina ( 1913 m ) , Trebević ( 1627 m ) , Treskavica ( 2088 m ) .
Duża część miasta położona jest na stromych stokach , co wpływa na zabudowę i układ ulic .
