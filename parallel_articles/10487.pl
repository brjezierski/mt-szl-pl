Saran ( polimer )
Saran – zastrzeżona nazwa handlowa folii opracowanych przez koncern Dow Chemicals i wykonanych z polietylenu niskiej gęstości ( LDPE ) oraz polibutylenu ( PB ) .
Do roku 2004 wykonywano je z kopolimeru poli ( chlorku winylidenu ) ( PVDC ; ≤85 % ) i poli ( chlorku winylu ) ( PVC ; ≥15 % ) .
W powszechnym użyciu nazwa ta dotyczy także innych tworzyw termoplastycznych opartych o PVDC .
Wykazuje bardzo niską przepuszczalność dla gazów .
Stosowany jest jako folia do zabezpieczania żywności oraz pakowania , a także jako włókna syntetyczne o zawartości PVDC ≥80 % .
