Sarandí
Sarandí – miasto w Argentynie , w prowincji Buenos Aires , w Partido Avellaneda .
Nazwa miasta pochodzi od lokalnej rośliny .
Sport .
W mieście swoją siedzibę ma klub piłkarski Arsenal de Sarandi , zdobywca Copa Sudamericana z roku 2007 .
Znajduje się tutaj stadion Estadio Julio Humberto Grondona o pojemności 16 300 , na którym drużyna rozgrywa swoje spotkania .
W mieście urodził się Rodrigo de Paul , mistrz świata w piłce nożnej z roku 2022 .
