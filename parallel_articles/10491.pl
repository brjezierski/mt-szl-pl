Kolczakówka białobeżowa
Kolczakówka białobeżowa ( `` Hydnellum underwoodii '' Banker ) – gatunek grzybów należący do rodziny kolcownicowatych ( `` Bankeraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hydnellum , Bankeraceae , Thelephorales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1906 r. Howard James Banker .
Synonimy :
Nazwa polska na podstawie rekomendacji Komisji ds .
Polskiego Nazewnictwa Grzybów przy Polskim Towarzystwie Mykologicznym .
W niektórych opracowaniach występuje pod nazwą sarniak białobeżowy .
Morfologia .
Średnica 7–10 cm .
Powierzchnia czerwonawo-brązowa , potem żółtawo-brązowa , pokryta łuskami , które z wiekiem stają się coraz większe .
