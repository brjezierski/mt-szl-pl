Setniczka zwyczajna
Setniczka zwyczajna ( `` Sarcogyne regularis '' Körb . )
– gatunek grzybów z rodziny `` Acarosporaceae '' .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Acarosporaceae , Acarosporales , Acarosporomycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Niektóre synonimy nazwy naukowej :
Nazwa polska według `` Krytycznej listy porostów i grzybów naporostowych Polski '' .
Morfologia .
