Sardynia
Sardynia ( , ) – skalista wyspa , druga pod względem powierzchni na Morzu Śródziemnym ( po Sycylii ) .
Wraz z pobliskimi wyspami tworzy region administracyjny we Włoszech .
Geografia .
Sardynia jest drugą co do wielkości wyspą na Morzu Śródziemnym .
Wybrzeża są najczęściej wysokie i skaliste , z długimi , względnie prostymi odcinkami , wieloma wybitnymi cyplami , kilkoma szerokimi , głębokimi zatokami , wieloma przesmykami i z wieloma mniejszymi wyspami nieopodal wybrzeża .
Przeważającą część wyspy zajmują góry i płaskowyże .
Roślinność stanowią głównie wiecznie zielone zarośla – makia .
Największymi rzekami są Tirso ( 151 km ) , wpadająca do Morza Sardyńskiego , Coghinas ( 115 km ) i Flumendosa ( 127 km ) .
Jedynym naturalnym słodkowodnym jeziorem jest Lago di Baratz .
Są 54 sztuczne jeziora i tamy , które dostarczają wodę i prąd .
Najważniejszymi są Lago Coghinas i Lago Omodeo .
Wzdłuż wybrzeża jest wiele dużych , płytkich , słonowodnych lagun i rozlewisk .
Wyspa ma typowy klimat podzwrotnikowy śródziemnomorski : zimą łagodny i deszczowy , latem gorący i suchy .
W ciągu roku jest około 300 dni słonecznych , z koncentracją opadów zimą i jesienią , i opadami śniegu w górach .
