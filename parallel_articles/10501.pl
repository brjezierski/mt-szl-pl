Sassari
( wym .
, pol .
uproszczona : `` sassari '' ; sass .
`` Sassari '' , ) – miasto i gmina we Włoszech , w regionie Sardynia , w prowincji Sassari .
Według danych na rok 2019 gminę zamieszkuje 125.273 osób , 229 os./km² .
Historia .
Miasto powstało w X w. , jako schronienie ludności nadmorskiej przed atakami Saracenów .
Na początku XIII wieku było to największe miasto niezależnego państewka o nazwie Giudicato di Torres i posiadało dużą autonomię .
