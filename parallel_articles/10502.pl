Satoshi Tajiri
– projektant gier , twórca serii gier `` Pokémon '' .
Życiorys .
Jako dziecko , Satoshi żył w dzielnicy Tokio i uwielbiał kolekcjonować owady , które wyszukiwał w mieście .
Urządzał pojedynki pomiędzy zebranymi owadami , które nazywał .
Był nazywany `` Dr. Bug '' ( `` Dr. Robal '' ) .
Tajiri nie lubił szkoły .
Jego ojciec chciał , żeby został elektrykiem , ale on tego nie chciał .
Pod koniec lat 70. pola i stawy , na których Satoshi poszukiwał owadów zostały przygotowane pod budowę bloków mieszkalnych i parkingów .
Praktycznie cały czas w technikum przeznaczał na gry konsolowe .
Był fanem gry Space Invaders , w którą chętnie grał w domu .
Kariera .
