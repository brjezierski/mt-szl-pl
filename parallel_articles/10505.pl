Arabia Saudyjska
Arabia Saudyjska ( , trb .
`` As-Su ’ udijja '' ) , Królestwo Arabii Saudyjskiej ( arab .
, trb .
`` Al-Mamlaka al-Arabijja as-Su ’ udijja '' ) – państwo położone w zachodniej Azji , na Półwyspie Arabskim .
Graniczy z Irakiem , Jordanią , Kuwejtem , Omanem , Katarem , Zjednoczonymi Emiratami Arabskimi i Jemenem .
Stolica znajduje się w Rijadzie .
Ważniejszymi miastami w Arabii Saudyjskiej są ( dane z 2021 roku ) : Rijad ( 7 387 817 mieszkańców ) , Dżudda ( 4 696 535 mieszkańców ) , Mekka ( 2 078 766 mieszkańców ) , Medyna ( 1 517 501 mieszkańców ) , Ad-Dammam ( 1 279 092 mieszkańców ) .
Arabia Saudyjska graniczy z Jordanią ( 744 km ) , Irakiem ( 814 km ) , Kuwejtem ( 222 km ) , Katarem ( 60 km ) , Zjednoczonymi Emiratami Arabskimi ( 457 km ) , Omanem ( 676 km ) i Jemenem ( 1458 km ) .
Arabia Saudyjska podzielona jest na 13 prowincji .
Geografia .
Roślinność występuje tylko w oazach .
W Arabii Saudyjskiej panuje gorący i suchy klimat pustynny – zwrotnikowo-kontynentalny .
Na zachodzie znajdują się góry Dżabal al-Hidżaz , w których znajduje się najwyższy szczyt Arabii Saudyjskiej – Dżabal as-Sauda mierzący do 3132 m. Część centralna jest wzniesiona do 1000-1400 m i łagodnie opada ku Zatoce Perskiej .
Na całym obszarze kraju przeważają pustynie kamieniste i pustynie piaszczyste poprzecinane wyschniętymi korytami rzek .
Największymi pustyniami są : Wielki Nefud , Mały Nefud , Ar-Rub al-Chali ( największa pustynia piaszczysta na Ziemi ) .
Wybrzeże Morza Czerwonego jest skaliste ze stromymi urwiskami ograniczającymi pas nadbrzeżnej równiny Tihama .
Wzdłuż wybrzeża znajduje się rafa koralowa .
Wybrzeże Zatoki Perskiej jest płaskie , zabagnione i piaszczyste .
Przy wybrzeżu znajdują się liczne przybrzeżne wysepki i rafy koralowe .
Temperatura w styczniu wynosi od 10 °C ( północ ) do 18 °C ( południe ) , a w lipcu od 30-35 °C do 50 °C .
Na północy pojawiają się przygruntowe przymrozki , a nawet mrozy .
Opady są sporadyczne , głównie w półroczu zimowym .
Średnia roczna suma opadów nie przekracza 100 mm .
