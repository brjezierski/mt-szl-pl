( 1525 ) Savonlinna – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 156 dni w średniej odległości 2,7 au .
Została odkryta 18 września 1939 ] roku w Obserwatorium Iso-Heikkilä w Turku przez Yrjö Väisälä .
Nazwa planetoidy pochodzi od Savonlinny , miasta w Finlandii .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 1525 ) 1939 SC .
