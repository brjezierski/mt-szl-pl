Toponimia .
Jego nazwa , „ Albania ” , jest pochodzenia grecko-łacińskiego i oznacza w tych językach „ górzysty kraj ” .
Według Pliniusza stolicą królestwa była Qəbələ , obecnie pozostały po niej ruiny oraz nazwa jednego z rejonów administracyjnych Azerbejdżanu : Quabala .
Strabon opisywał , że zamieszkują to królestwo ludy posługujące się 26 językami .
Według Roberta Hewsena były to ludy ibero-kaukaskie , które być może dzięki warunkom terenowym ( analogia z Baskami ) nie uległy asymilacji przez Indoeuropejczyków , którzy zdominowali rozległe obszary Europy i Azji zachodniej .
