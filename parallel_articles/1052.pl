z Partami .
W latach 252-253 kraj został podbity przez Sasanidów , stając się jedną z satrapii ( satrapia Albanii ) .
W roku 387 dokonano podziału Albanii między Sasanidów a Bizancjum .
W 451 r. miało miejsce nieudane powstanie przeciwko Persji .
W 504 roku cała Albania Kaukaska została podbita przez Sabirów lub Chazarów , a następnie odbita przez szacha Kawada .
Chazarowie zajęli ją ponownie w 552 roku i równie szybko utracili na rzecz Persów .
W połowie VII wieku przeszła pod panowanie arabskie , od tej pory nie używano już tej nazwy w kontekście regionu czy jednostki politycznej , nowi władcy zjednoczyli kraj z Armenią .
