Sawadaea bicornis
Sawadaea bicornis ( Wallr . )
Homma – gatunek grzyba z rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożytuje na klonach , powodując u nich chorobę zwaną mączniakiem prawdziwym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sawadaea , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1819 r. Carl Friedrich Wilhelm Wallroth nadając mu nazwę `` Alphitomorpha bicornis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Yasu Homma w 1937 r .
