Sawadaea tulasnei
Sawadaea tulasnei ( Fuckel ) Homma – gatunek grzyba z rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożytuje na niektórych gatunkach klonów , powodując u nich chorobę zwaną mączniakiem prawdziwym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sawadaea , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1866 r. Karl Wilhelm Gottlieb Leopold Fuckel nadając mu nazwę `` Uncinula tulasnei '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Yasu Homma w 1937 r .
Synonimy :
Morfologia .
