Scarlett Johansson
Scarlett Ingrid Johansson ( ur .
22 listopada 1984 w Nowym Jorku ) – amerykańska aktorka filmowa , telewizyjna i teatralna , scenarzystka oraz piosenkarka .
Przełomowe okazały się dla niej role w `` Manny i Lo '' ( 1996 ) , gdzie zagrała pierwszą główną rolę , oraz w `` Zaklinaczu koni '' ( 1998 ) , w którym zagrała u boku Roberta Redforda .
Od tamtej pory zagrała m.in .
w filmach Sofii Coppoli , Petera Webbera , Briana De Palmy czy Luca Bessona .
Wystąpiła w trzech filmach Woody ’ ego Allena oraz w kilku produkcjach franczyzy `` Filmowego Uniwersum Marvela '' jako Natasha Romanoff / Czarna Wdowa .
Za swoje role została wyróżniona m.in .
nagrodami : BAFTA , Tony , Gotham czy Saturn oraz była czterokrotnie nominowana do Złotych Globów .
