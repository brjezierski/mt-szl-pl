Sceaux ( Hauts-de-Seine )
Sceaux – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Według danych na rok 2012 gminę zamieszkiwały 19 986 osoby , a gęstość zaludnienia wynosiła 5552 osób/km² .
Wśród 1287 gmin regionu Île-de-France Sceaux plasuje się na 778. miejscu pod względem powierzchni .
W Sceaux ślub wzięli Piotr Curie i Maria Skłodowska , których wesele odbyło się tamże w ogrodzie domu jego rodziców .
Początkowo zostali także tu pochowani do czasu przeniesienia szczątków 20 kwietnia 1995 do paryskiego Panteonu .
Miejsce urodzenia francuskiego aktora Alaina Delona .
