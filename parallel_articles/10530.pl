Szafuza
Szafuza ( niem .
`` Schaffhausen '' ) – miasto ( 33 tys .
mieszkańców ) w północnej Szwajcarii nad Renem , położone przy granicy z Niemcami .
Stolica kantonu o tej samej nazwie .
W mieście swoją siedzibę ma firma informatyczna Acronis .
Historia .
Szafuza biła własną monetę od 1005 r. , gdy została niezależnym państwem-miastem .
Przez pewien okres znajdowała się pod panowaniem Habsburgów , jednak odzyskała niezależność w 1415 r. W 1457 r. zjednoczyła się z Zurychem , a w 1501 r. została pełnoprawnym członkiem Konfederacji Szwajcarskiej .
W 1857 r. doprowadzono do Szafuzy kolej .
W 1869 roku Amerykanin Florentine Ariosto Jones założył tu fabrykę zegarków IWC .
1 kwietnia 1944 roku miasto ucierpiało z powodu omyłkowego amerykańskiego nalotu 8 Armii Powietrznej , w wyniku którego zginęło 37 osób .
