Rozszczepka kloszowa
Rozszczepka kloszowa , uszaczek kosmaty ( `` Schizophyllum amplum '' Fr . )
– gatunek grzybów należący do rodziny rozszczepkowatych ( `` Schizophyllaceae '' )
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Schizophyllum , Schizophyllaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go Joseph-Henri Léveillé w 1848 r. nadając mu nazwę `` Cyphella ampla '' .
Obecną , uznaną przez Index Fungorum nazwę nadała mu Karen K. Nakasone w 1996 r .
Ma 15 synonimów .
Niektóre z nich :
Władysław Wojewoda w 1973 r. nadał polską nazwę uszaczek kosmaty ( wówczas gatunek ten zaliczany był do rodzaju `` Auriculariopsis '' , czyli uszaczek ) .
Po przeniesieniu gatunku do rodzaju `` Schizophyllum '' nazwa ta stała się niespójna z nazwą naukową .
Nazwę rozszczepka kloszowa podaje internetowy atlas grzybów .
