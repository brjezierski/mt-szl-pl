Drewniczka drobnopora
Drewniczka drobnopora ( `` Xylodon flaviporus '' ( Berk .
& amp ; M.A .
Curtis ex Cooke ) Riebesehl & amp ; Langer ) – gatunek grzybów z rzędu szczeciniakowców ( `` Hymenochaetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Xylodon , Schizoporaceae , Hymenochaetales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten w 1886 r. opisali Miles Joseph Berkeley , Moses Ashley Curtis i Mordecai Cubitt Cooke nadając mu nazwę `` Poria flavipora '' .
Później zaliczany był do różnych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadali mu J. Riebesehl i E. Langer w 2017 r .
Ma 24 synonimy .
Niektóre z nich :
Barbara Gumińska i Władysław Wojewoda w 1983 r. nadali mu polską nazwę drewniczka drobnopora , W. Wojewoda w 1971 r. zmienił ją na drewniczek czyreniowy , a w 2003 r. na strzępkoząb żółtopory .
Gatunek ten w tym czasie zaliczany był do rodzaju `` Schizopora '' , czyli drewniczka , lub `` Hyphodontia '' , czyli strzępkoząb .
Po przeniesieniu go do rodzaju `` Xylodon '' wszystkie nazwy polskie stały się niespójne z nazwą naukową .
Morfologia .
Rozpostarty lub rozpostarto-odgięty o długości do 20 cm , wyjątkowo do 40 .
Powierzchnia brudnobiała , żółta do pomarańczowo-ochrowej , często różowa .
Brzeg biały i odstający , na brzegach może również tworzyć struktury przypominające kapelusze .
