Schizothyrium pomi
Schizothyrium pomi ( Mont .
& amp ; Fr . )
Arx – gatunek grzybów z klasy `` Dothideomycetes '' .
Wywołuje kropkowaną plamistość jabłek .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Schizothyrium , Schizothyriaceae , Incertae sedis , Incertae sedis , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w roku 1834 Jean Pierre François Camille Montagne i Elias Fries , nadając mu nazwę `` Labrella pomi '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1959 Josef Adolph von Arx .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
