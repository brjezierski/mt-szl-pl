Schlettau
Schlettau − miasto w Niemczech , w kraju związkowym Saksonia , w powiecie Erzgebirgskreis ( do 31 lipca 2008 w powiecie Annaberg ) , wchodzi w skład wspólnoty administracyjnej Scheibenberg-Schlettau .
Do 29 lutego 2012 należało do okręgu administracyjnego Chemnitz .
Geografia .
Schlettau leży w paśmie gór Rudawy , ok. 5 km na południowy zachód od miasta Annaberg-Buchholz .
Jedyną dzielnicą miasta jest Dörfel .
