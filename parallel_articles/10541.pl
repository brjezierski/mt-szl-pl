Schmölln
Schmölln – miasto w Niemczech , w kraju związkowym Turyngia , w powiecie Altenburger Land .
Liczy 13 741 mieszkańców ( 31 grudnia 2018 ) .
1 stycznia 2019 do miasta przyłączono trzy gminy ze zlikwidowanej dzień wcześniej wspólnoty administracyjnej Altenburger Land : Altkirchen , Drogen oraz Lumpzig .
Gminy te stały się automatycznie jego dzielnicami .
Dodatkowo od tego samego dnia miasto pełni funkcję `` gminy realizującej '' ( niem .
`` erfüllende Gemeinde '' ) dla gminy wiejskiej Dobitschen .
