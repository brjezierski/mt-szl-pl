Schorndorf ( Badenia-Wirtembergia )
Schorndorf – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Stuttgart , w powiecie Rems-Murr , siedziba wspólnoty administracyjnej Schorndorf .
Leży nad rzeką Rems , ok. 15 km na wschód od Waiblingen , przy drodze krajowej B29 i linii kolejowej Stuttgart–Aalen .
