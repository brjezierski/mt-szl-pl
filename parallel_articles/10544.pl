Schwalbach am Taunus
Schwalbach am Taunus – miasto w Niemczech , w kraju związkowym Hesja , w rejencji Darmstadt , w powiecie Main-Taunus .
Leży u podnóża gór Taunus , niedaleko Frankfurtu nad Menem .
Zajmuje powierzchnię 6,47 km² i liczy mieszkańców ( 30 września 2015 ) .
Historia .
Historia miejscowości sięga roku 782 , jednakże prawa miejskie nabyła dopiero w maju 1970 .
W Schwalbach am Taunus znajdują się 4 szkoły ogólnokształcące , szkoła zawodowa , 8 przedszkoli , hala sportowa , 4 szkolne hale sportowe , 7 boisk , kompleks kortów tenisowych , basen otwarty i kryty , kompleks mieszkalno-rozrywkowy dla seniorów , szkoła muzyczna i szkoła baletowa .
Miasto zamieszkuje duża liczba obcokrajowców , którzy stanowią 13,2 % ludności .
