Schwarzenberg/Erzgeb .
Schwarzenberg/Erzgeb. , Schwarzenberg/Erzgebirge – miasto w Niemczech , w kraju związkowym Saksonia , w okręgu administracyjnym Chemnitz , w powiecie Erzgebirgskreis ( do 31 lipca 2008 w powiecie Aue-Schwarzenberg ) .
Geografia .
Schwarzenberg leży ok. 15 km na południowy wschód od miasta Aue .
Historia .
Najstarsza wzmianka o mieście pochodzi z 1282 .
W 1533 miasto zakupił elektor saski Jan Fryderyk I. W latach 1690–1699 wzniesiono barokowy kościół św .
Jerzego .
Od 1697 do 1763 miasto znajdowało się pod panowaniem królów Polski Augusta II Mocnego i Augusta III Sasa .
Pamiątkami po tym okresie są herb Polski zdobiący lożę króla Augusta II w kościele św .
Jerzego oraz pocztowy słup dystansowy z 1725 z inicjałami króla Augusta II w dzielnicy Crandorf .
Od 1806 w granicach Królestwa Saksonii , połączonego w latach 1807–1815 unią personalną z Księstwem Warszawskim .
W 1871 włączone do Niemiec .
W latach 1949–1990 część NRD .
