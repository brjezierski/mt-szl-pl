Schweinfurt
Schweinfurt – miasto na prawach powiatu w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Frankonia , w regionie Main-Rhön , siedziba powiatu Schweinfurt , do którego jednak miasto nie należy .
Leży nad Menem , zamieszkuje je 51 851 mieszkańców ( 31 grudnia 2013 ) .
Najbliżej położone duże miasta : Norymberga ok. 100 km na południowy wschód , Frankfurt nad Menem – ok. 150 km na zachód , Lipsk – ok. 200 km na północ .
W mieście znajduje się stacja kolejowa Schweinfurt Hauptbahnhof .
