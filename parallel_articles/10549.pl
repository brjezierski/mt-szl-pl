Schwäbisch Gmünd
Schwäbisch Gmünd – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , siedziba regionu Ostwürttemberg , w powiecie Ostalb , również siedziba wspólnoty administracyjnej Schwäbisch Gmünd .
Leży nad rzeką Rems , ok. 25 km na południe od Aalen , przy drodze krajowej B29 i linii kolejowej Stuttgart-Aalen .
W mieście znajduje się stacja kolejowa Schwäbisch Gmünd .
