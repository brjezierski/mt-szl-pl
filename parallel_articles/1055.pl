Albert Barillé
Albert Barillé ( ur .
14 lutego 1920 w Warszawie , zm .
5 lutego 2009 w Neuilly-sur-Seine ) – francuski producent telewizyjny , scenarzysta , karykaturzysta i założyciel studia filmowego Procidis .
Był twórcą takich seriali animowanych jak `` Colargol '' oraz serii `` Było sobie ... '' .
Był również autorem programów medycznych i dokumentalnych , sztuk teatralnych , a także popularyzatorem filozofii .
