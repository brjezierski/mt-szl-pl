Scott Dixon
Scott Ronald Dixon ( ur .
22 lipca 1980 w Brisbane ) – nowozelandzki kierowca wyścigowy , sześciokrotny mistrz serii IndyCar Series z 2003 , 2008 , 2013 , 2015 , 2018 i 2020 roku .
Zwycięzca Indianapolis 500 2008. .
Kariera .
Początki kariery .
Karierę wyścigową rozpoczął w wieku 10 lat od kartingu .
W wieku 13 lat dostał specjalne pozwolenie na ściganie się w wyścigach samochodów turystycznych ( w Nowej Zelandii prawo jazdy można uzyskać dopiero w wieku 15 lat i 6 miesięcy ) .
W kolejnych latach przechodził przez kolejne szczeble juniorskich formuł w swoim kraju , aż w końcu w 1997 roku wystartował w australijskiej Formule Holden .
