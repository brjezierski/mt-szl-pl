Scott Joplin
Scott Joplin ( ur .
24 listopada 1868 w Marshall , zm .
1 kwietnia 1917 w Nowym Jorku ) – amerykański kompozytor i pianista , popularyzator stylu ragtime .
Życiorys .
Prawdopodobnie miejsce urodzenia to Texarkana w stanie Teksas , przy czym biograf muzyka Edward Berlin podważa dotychczasowe ustalenia co do miejsca i daty jego urodzenia .
Miasteczko Texarkana uważane powszechnie za miejsce narodzin Joplina nie istniało jeszcze przez kolejnych 5 lat .
Spisy ludności świadczyć mają o urodzeniu Joplina co najmniej pół roku wcześniej .
Joplin przyszedł na świat najprawdopodobniej w północnym Teksasie , w miejscowości Marshall , która w spisach podana była jako pierwsze miejsce zamieszkania rodziny .
Wraz z różnymi grupami muzycznymi podróżował po miastach Środkowego Zachodu , grając w klubach .
Na początku XX wieku osiadł w Nowym Jorku , gdzie w 1917 umarł na kiłę .
Zasłynął jako twórca pierwszej opery afroamerykańskiej – `` A Guest of Honor '' .
W 1908 został opublikowany jego podręcznik `` The School of Ragtime - Six Exercises for Piano '' .
Kojarzony jest przede wszystkim z kompozycjami ragtime : `` Maple Leaf Rag '' z 1899 i `` The Entertainer '' z 1902 ( spopularyzowaną dzięki filmowi `` Żądło '' – `` The Sting '' z 1973 ) .
