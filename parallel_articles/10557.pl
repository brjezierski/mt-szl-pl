Scranton ( Pensylwania )
Scranton – miasto w Stanach Zjednoczonych , w stanie Pensylwania , nad rzeką Lackawanna .
Historia .
W 1776 roku tereny obecnego miasta Scranton były zamieszkiwane przez rdzennych Amerykanów .
Z czasem w Scranton zaczęli osiedlać się osadnicy z Nowej Anglii , budowali oni liczne młyny i zakładali małe przedsiębiorstwa wytwórcze .
W roku 1845 w Scranton rozpoczęła się produkcja żelaza i stali , z czasem miasto stało się wiodącym producentem żelaznych szyn .
