Naziemek kozionogi
Naziemek kozionogi ( `` Scutiger pes-caprae '' ( Pers . )
Bondartsev & amp ; Singer ) – gatunek grzybów z rzędu gołąbkowców ( `` Russulales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Scutiger , Albatrellaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten w 1818 r. opisał Christiaan Hendrik Persoon .
Później zaliczany był do różnych rodzajów .
Obecną nazwę nadali mu Appollinaris Semenovich Bondartsev i Rolf Singer w 1941 r .
Ma 13 synonimów .
