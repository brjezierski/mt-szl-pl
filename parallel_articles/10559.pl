W 1967 r. Stanisław Domańskki nadał mu polską nazwę naziemek kozionogi , w 1983 r. Barbara Gumińska i Władysław Wojewoda zmienili ją na bielaczek czerwonawy , w 2003 r. W. Wojewoda zakkceptował nazwę podaną przez S. Domańskiego .
Po przeniesieniu do rodzaju `` Scutiger '' nazwy te są niespójne z nazwą naukową .
