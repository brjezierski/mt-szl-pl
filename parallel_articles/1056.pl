Albert Bierstadt
Albert Bierstadt ( ur .
7 stycznia 1830 w Solingen , zm .
w 18 lutego 1902 w Nowym Jorku ) – amerykański malarz pochodzenia niemieckiego , pejzażysta , reprezentant Hudson River School .
Biografia .
Albert Bierstadt urodził się w Solingen w Królestwie Prus 7 stycznia 1830 roku .
gdy miał dwa lata rodzina wyemigrowała do Stanów Zjednoczonych , osiadając w New Bedford w stanie Massachusetts .
Ojciec malarza , Henry Bierstad , znalazł zatrudnienie jako bednarz .
Bierstadt początkowo był samoukiem .
W 1850 roku zaczął reklamować swoje usługi jako instruktor rysunku .
W 1853 odbył podróż do Europy .
Przy wsparciu Emanuela Leutze ’ a i Worthingtona Whittredge ’ a ukończył studia malarskie w Szkole Sztuk Pięknych w Düsseldorfie .
Wraz z Whittredge ’ em odbył podróż po Europie .
Jesienią 1857 roku powrócił do Ameryki .
Na wiosnę 1858 roku artysta zaprezentował na sezonowej wystawie w National Academy of Design w Nowym Jorku obraz `` Lake Lucerne '' , który zyskał uznanie i zapoczątkował drogę narodowej kariery .
