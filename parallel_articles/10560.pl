Scytinostroma hemidichophyticum
Scytinostroma hemidichophyticum Pouzar – gatunek grzybów z rodziny powłocznicowatych ( `` Peniophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Scytinostroma , Peniophoraceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1966 r. Zdeněk Pouzar na martwym drewnie buka w Czechach .
Morfologia .
`` S. hemidichophyticum '' tworzy rozpostarte , wieloletnie owocniki o silnym zapachu naftalenu .
System strzępkowy słabo rozwinięty , strzępki szkieletowe dekstrynoidalne .
W hymenium występują obfite , długie dendrohyfidy .
Bazydiospory kuliste lub prawie kuliste , gładkie , amyloidalne .
Podobna jest skórówka kulistozarodnikowa ( `` Scytinostroma portentosum '' ) .
Odróżnia się słabo rozgałęzionymi dendrohyfidami , które mają bardzo długie zakończenia i ułożone są w hymenium równolegle do podłoża .
