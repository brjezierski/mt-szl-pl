Seaside ( Kalifornia )
Seaside , wcześniej East Monterey – miasto w Stanach Zjednoczonych , w stanie Kalifornia , w hrabstwie Monterey .
Jest siedzibą California State University , Monterey Bay ( CSUMB ) i Monterey College of Law , które znajdują się na terenie dawnej bazy wojskowej Fort Ord .
Geografia .
