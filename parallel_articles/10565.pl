Seattle
Seattle – największe miasto w stanie Waszyngton , ośrodek administracyjny hrabstwa King , położone pomiędzy zatoką Puget a jeziorem Washington , około 175 km na południe od granicy z Kanadą .
W 2015 roku miasto liczyło 684 451 mieszkańców .
Miasto jest ważnym morskim i lotniczym portem towarowym i osobowym w handlu z państwami azjatyckimi .
Zespół miejski Seattle-Everett-Tacoma liczy ponad 3,4 mln mieszkańców .
Dane ogólne .
Nazwa miasta pochodzi od zniekształcenia imienia Si ’ ahl , wodza okolicznych plemion Duwamish i Suquamish , zwanego częściej Chief Seattle ( Wódz Seattle ) .
Oficjalnie nazywane jest również „ Szmaragdowym Miastem ” ( ang .
Emerald City ) .
Osada europejskich osadników powstała około 1851 .
W czerwcu 1889 centrum miasta uległo pożarowi nazwanemu `` The Great Seattle Fire '' .
Władze miasta podjęły dwie ważne decyzje związane z odbudową .
Po pierwsze wszystkie nowe budynki musiały powstać z kamienia lub cegły , po drugie powinny one być wyższe przynajmniej o jedno piętro niż poprzednie .
W ciągu kolejnych lat nowe drogi budowano od 3,7 do nawet 9 metrów wyżej niż poprzednie , było to podyktowane wysokim poziomem wód gruntowych .
Tak właśnie w Seattle powstały charakterystyczne piwnice .
Spacerując obecnie po centrum Seattle , przechodzień znajdzie się często na zewnątrz na poziomie drugiego piętra .
Żaden z budynków Seattle pierwotnie nie posiadał piwnic .
Jeszcze do dziś w „ piwnicach ” istnieją dawne witryny sklepów i widoczne są okna instytucji i mieszkań .
Demografia .
Według spisu z 2000 roku , miasto zamieszkane było przez 563 374 osoby , które tworzyły 258 499 gospodarstw domowych oraz 113 400 rodzin .
67,1 % ludności miasta należy do białej rasy , 16,6 % to Azjaci , 9,7 % to Afroamerykanie , 2,38 % ludność innych ras , 1 % rdzenni Amerykanie , 4,46 % ludność wywodząca się z dwóch lub większej liczby ras , 1,5 % mieszkańcy wysp Pacyfiku , 6,3 % to Hiszpanie lub Latynosi .
