Łyżka do butów
Łyżka do butów jest narzędziem ułatwiającym zakładanie i zdejmowanie butów oraz zapobiegającym zagniataniu tylnych brzegów buta .
Zazwyczaj łyżki do butów wykonane są ze stali , niekiedy z plastiku .
Niektóre mają część roboczą osadzoną na kiju , aby umożliwić zakładanie i zdejmowanie butów bez schylania się .
Pierwotnie łyżki do butów wykonywano z rogów zwierzęcych , czego ślad pozostał do dziś w języku angielskim , gdzie nazwa tego przedmiotu brzmi `` shoehorn '' , w wolnym przekładzie `` róg do butów '' .
