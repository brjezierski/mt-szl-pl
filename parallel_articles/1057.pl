Albert Einstein
Albert Einstein ( wym . )
( ur .
14 marca 1879 w Ulm , zm .
18 kwietnia 1955 w Princeton ) – niemiecki fizyk teoretyk pochodzenia żydowskiego , noblista .
Posiadał również obywatelstwo Szwajcarii ( od 1901 ) i USA ( od 1940 ) .
Einstein zrewolucjonizował zarówno mechanikę , jak i teorię pola , głównie w wersji klasycznej , choć odegrał też kluczową rolę dla mechaniki kwantowej .
Laureat Nagrody Nobla w dziedzinie fizyki za 1921 rok , w uznaniu za „ wkład do fizyki teoretycznej , zwłaszcza opis prawa efektu fotoelektrycznego ” .
Einstein to twórca szczególnej teorii względności , która ulepszyła mechanikę Newtona i zastąpiła w tej korekcyjnej roli teorię eteru Lorentza .
Autor wynikającej z STW równoważności masy i energii , czasem formułowanej słynnym wzorem `` E = mc '' 2 .
Został on potwierdzony przez reakcje subatomowe , np .
jądrowe ; doprowadziło to do rewizji zasady zachowania masy , samego pojęcia materii i otworzyło epokę jądrowej broni oraz energetyki .
Twórca ogólnej teorii względności , która połączyła newtonowskie prawo powszechnego ciążenia z nową mechaniką .
Nie była to jedyna ani nawet pierwsza synteza tego typu , jednak OTW w odróżnieniu od konkurencji okazała się poprawna , zostając nowym paradygmatem w opisie grawitacji .
Einstein oparł na swojej teorii pierwsze modele kosmologiczne oraz pierwsze przewidywania grawitacyjnych fal czasoprzestrzeni .
Wprowadził też do niej opcjonalną stałą kosmologiczną , która później okazała się możliwym wyjaśnieniem ciemnej energii .
Teoria Einsteina dzięki tym i innym wynikom doprowadziła do rozkwitu astrofizyki w XX wieku .
Naukowiec przewidział również istnienie fotonu – postulując dualizm korpuskularno-falowy światła .
