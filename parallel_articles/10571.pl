Sede vacante
Sede vacante ( od łac .
`` sedes vacans '' – „ nieobsadzona stolica ” lub „ pusty tron ” ; forma `` sede vacante '' to ablativus absolutus i znaczy : „ przy nieobsadzonej stolicy ” ; pokr .
`` sedis vacantia '' – „ stan nieobsadzenia stolicy ” ) – w Kościele katolickim termin określający okres , w którym stolica biskupia jest nieobsadzona .
Kodeks prawa kanonicznego z 1983 stosuje to określenie zarówno wobec wakansu Stolicy Apostolskiej ( diecezji rzymskiej – `` sede Romana vacante '' ) , jak i każdej innej diecezji .
`` Sede vacante '' trwa do momentu objęcia diecezji przez nowego biskupa .
