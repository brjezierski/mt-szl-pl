Seefeld in Tirol
Seefeld in Tirol – gmina w Austrii w kraju związkowym Tyrol , w powiecie `` Bezirk Innsbruck-Land '' .
Ośrodek sportów zimowych .
W Seefeld in Tirol w 1963 odbyły się Mistrzostwa Świata w Biathlonie oraz konkursy skoków narciarskich i biegów na Zimowych Igrzyskach Paraolimpijskich w Innsbrucku .
W gminie tej odbyły się także w 1985 Mistrzostwa Świata w Narciarstwie Klasycznym .
Kolejne Mistrzostwa Świata w Narciarstwie Klasycznym odbyły się w tej gminie w 2019 roku .
