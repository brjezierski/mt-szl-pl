Seevetal
Seevetal – miejscowość i gmina w Niemczech , w kraju związkowym Dolna Saksonia , w Powiecie Harburg , leży na południe od Hamburga .
W 2008 roku Seevetal liczyło 41 560 mieszkańców i tym samym jest najludniejszą gminą w całych Niemczech nie posiadającą praw miejskich .
Nazwa pochodzi od rzeki Seeve , małego lewego dopływu Łaby .
