Seifertia azaleae
Seifertia azaleae ( Peck ) Partr .
& amp ; Morgan-Jones – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt różaneczników wywołujący chorobę o nazwie zamieranie pąków różanecznika .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Seifertia , Incertae sedis , Incertae sedis , Incertae sedis , Incertae sedis , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1873 r. Ch.H .
Peck nadając mu nazwę `` Periconia azaleae '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2002 r. Partr .
& amp ; Morgan-Jones .
Znany jest tylko w postaci anamorfy .
Synonimy :
Morfologia i rozwój .
Patogen rozwija się w pąkach różaneczników wytwarzając na ich zewnętrznych , obumarłych łuskach koremia o rozmiarach 2 mm × 0,5 mm .
Mają postać czarnych pałeczek na długich trzonkach .
Powstają w nich zarodniki konidialne o średnicy 3,6 do 5,5 μm .
Grzybnię `` Seifertia azaleae '' można hodować na agarze skrobiowym z siarczanem streptomycyny w stężeniu 100 mg/l .
Potwierdzono patogeniczność trzech izolatów przez zaszczepienie zawiesiną zarodników 30 pąków 2 roślin rododendronów .
Zainfekowane pąki osłonięto woreczkami foliowymi .
Porażone rośliny wytwarzały typowe objawy choroby po 20 dniach od zakażenia .
Różne odmiany różaneczników wykazywały różną podatność na patogena .
