Sejo ( ur .
2 listopada 1417 , zm .
23 września 1468 ) – siódmy władca ( `` wang '' ) Korei ( Chosŏn ) z Dynastii Ri .
Objął tron poprzez zamach stanu .
Życiorys .
Urodzony w 1417 roku drugi syn Sejonga Wielkiego , wykazywał się umiejętnością łucznictwa , jazdy konnej i sztuk walki .
Był także zdolnym dowódcą wojskowym , choć osobiście nie uczestniczył w wojnach .
W 1428 został Wielkim Księciem o imieniu Suyang .
