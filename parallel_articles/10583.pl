Seks
Seks ( łac .
`` sexus '' , płeć ) – ogół zachowań wynikających z popędu płciowego i mających na celu zaspokojenie indywidualnych potrzeb seksualnych człowieka ; potocznie : stosunek płciowy , w tym także seks oralny lub analny .
Zachowania seksualne mogą zachodzić przy kontakcie dwóch osób płci przeciwnej ( o orientacji heteroseksualnej lub biseksualnej ) lub tej samej płci ( o orientacji homoseksualnej lub biseksualnej ) , a także przy kontakcie grupy ludzi ( zob .
seks grupowy ) , o tej samej lub różnej płci ( zob .
poliamoria ) .
Zachowania seksualne .
Statystyki wskazują , że tylko pięć procent ludzi kocha się codziennie , a co czwarta osoba – raz w tygodniu .
Oprócz stosunków płciowych , ludzi cechuje też wiele innych zachowań seksualnych .
