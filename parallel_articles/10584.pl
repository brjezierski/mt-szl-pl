Sekursko
Sekursko – wieś w Polsce , położona w województwie łódzkim , w powiecie radomszczańskim , w gminie Żytno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa częstochowskiego .
We wsi znajduje się zespół dworsko-parkowy .
Budynek dworu pochodzi z początku XX wieku i do końca II wojny światowej należał do rodziny Biedrzyckich .
