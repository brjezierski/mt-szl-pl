Semarang
Semarang – miasto w Indonezji na północnym wybrzeżu Jawy u ujścia rzeki Semarang do Morza Jawajskiego ; współrzędne geograficzne .
Ośrodek administracyjny prowincji Jawa Środkowa ; powierzchnia 373,7 km2 ; 1,66 mln mieszkańców ( 2022 ) .
Siedziba rzymskokatolickiej archidiecezji Semarang .
Przemysł włókienniczy , spożywczy , stoczniowy , elektrotechniczny , rzemiosło artystyczne .
Ważny węzeł komunikacyjny ; port lotniczy `` Achmad Yani '' ; port morski Tanjung Emas , wywóz kopry , cukru , kawy , kauczuku , tytoniu .
Siedziba kilku uniwersytetów .
Historia .
Co najmniej od IX w. istniało tu miasto Bergota słynące ze zdolnych cieśli okrętowych .
W 1406 r. dotarł tu słynny chiński żeglarz Zheng He .
w 1678 r. kontrolę nad miastem przejęła Holenderska Kompania Wschodnioindyjska .
Od 1945 r. należy do Indonezji .
