Albigowa ( gmina )
Albigowa – dawna gmina wiejska istniejąca w latach 1973–1976 w woj .
rzeszowskim ( dzisiejsze woj .
podkarpackie ) .
Siedzibą władz gminy była Albigowa .
Gmina została utworzona 1 stycznia 1973 roku w woj .
rzeszowskim ( powiat łańcucki ) .
1 czerwca 1975 roku gmina znalazła się w nowo utworzonym mniejszym woj .
rzeszowskim .
2 lipca 1976 roku gmina została zniesiona przez połączenie jej terenów z gminami Kosina i Łańcut w gminę Łańcut .
