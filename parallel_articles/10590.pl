Semej
Semej ( ; , `` Siemiej '' ; do 2007 roku Semipałatyńsk ) – miasto w północno-wschodnim Kazachstanie , stolica obwodu abajskiego , w pobliżu granicy z Rosją , położone na południowy wschód od Pawłodaru .
