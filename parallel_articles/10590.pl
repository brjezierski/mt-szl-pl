Historia .
Założony w 1718 roku jako warownia położona 18 km na południe od obszaru dzisiejszego miasta , w pobliżu ruin klasztoru buddyjskiego , składającego się z siedmiu budynków , od którego wzięła się nazwa Semipałatyńsk .
Przeniesiony na swoje obecne miejsce w 1778 roku , w celu ochrony przed wiosennymi powodziami ; od 1782 roku miasto .
Od XIX wieku miejsce zesłań politycznych .
W latach 1854–1859 w mieście przebywał pisarz Fiodor Dostojewski , który służył jako żołnierz w batalionie liniowym .
Rozwój miasta nastąpił po doprowadzeniu linii kolejowej z Syberii w 1906 roku oraz po zakończeniu budowy Kolei Turkiestańsko-Syberyjskiej w 1931 roku .
