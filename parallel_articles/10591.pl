Siemieniszki
Siemieniszki – dawna wieś .
Tereny , na których leżała , znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie szarkowszczyńskim , w sielsowiecie Jody .
Historia .
W czasach zaborów wieś w gminie Jody , w powiecie dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
Pod koniec XIX należały do dóbr Paulinowo , własność Bożeranowych .
W latach 1921–1945 wieś leżała w Polsce , w województwie wileńskim , w powiecie dziśnieńskim ( od 1926 w powiecie brasławskim ) , w gminie Jody .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 70 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Było tu 13 budynków mieszkalnych .
W 1931 w 14 domach zamieszkiwało 71 osób .
Wierni należeli do parafii rzymskokatolickiej w Borodzieniczach .
Miejscowość podlegała pod Sąd Grodzki w Brasławiu i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Jodach .
