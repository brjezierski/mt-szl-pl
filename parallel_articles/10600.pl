Septoria apiicola
Septoria apiicola Speg .
– gatunek grzybów z klasy `` Dothideomycetes '' .
U selerów wywołuje chorobę o nazwie septorioza selera .
Gatunek kosmopolityczny , rozprzestrzeniony na całym świecie .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Grzyb mikroskopijny , pasożyt rozwijający się jako endobiont w tkankach roślin .
Tworzy kuliste , zanurzone w tkankach roślin pyknidia o barwie od brązowej do czarnej i średnicy 75–195 μm .
Mają perydium zbudowane z 3 warstw cienkościennych komórek pseudoparenchymy .
Komórki te są cienkościenne , jasnobrązowe i wyraźnie ciemniejsze w pobliżu okrągłej ostioli o średnicy 15-50 μm .
Konidiofory bezbarwne , bez przegród , cylindryczne , zakończone zgrubiałą fialidą .
Mają długość 8-10 μm i średnicę 3-3,5 μm .
Konidia bezbarwne , nitkowate , zazwyczaj z trzema , ale czasami także z 1 , 2 , 4 lub 5 przegrodami .
Mają lekko stożkowaty wierzchołek , tępą podstawę , długość 22-56 μm ( średnio 35 μm ) , szerokość 2-2,5 μm .
