Septoria convolvuli
Septoria convolvuli Desm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na kieliczniku zaroślowym ( `` Calystegium sepii '' ) i powoju polnym ( `` Convolvulus arvensis '' ) .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Występowanie .
Znane jest występowanie `` S. convolvuli '' w Ameryce Północnej ( USA i Kanada ) , w większości krajów Europy , licznych krajach Azji , w Ameryce Południowej ( Argentyna , Chile ) , Afryce ( Libia ) , Australii i Nowej Zelandii .
W piśmiennictwie naukowym na terenie Polski podano liczne stanowiska .
