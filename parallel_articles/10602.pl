Septoria digitalis
Septoria digitalis Pass .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Pasożyt roślin z rodzaju naparstnica ( `` Digitalis '' ) .
Grzyb mikroskopijny , endobiont rozwijający się w tkankach roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisał Giovanni Passerini w 1879 r .
Charakterystyka .
Na obydwu stronach porażonych liści powstają okrągławe lub wielokątne plamy o średnicy do 5 mm .
Początkowo są jasnobrązowe , potem brązowe z brązowofioletową obwódką .
W obrębie plam na górnej stronie liści tworzą się pyknidia o średnicy 40–140 μm .
Powstają podskórnie , lub mniej lub bardziej zanurzone w warstwie miękiszu palisadowego .
Na ich wewnętrznej ścianie na konidioforach powstają proste lub nieco wygięte , nitkowate konidia o wymiarach 12,5–58 × 1,2,2 μm bez przegród lub z 1–4 przegrodami.Wydostają się przez pojedynczą , otoczoną ciemniejszymi komórkami ostiolę o średnicy 20–60 μm .
Znane jest występowanie tego gatunku w niektórych krajach Europy ( Belgia , Bułgaria , Czechy , Holandia , dawna Jugosławia , Polska , Portugalia , Rosja , Rumunia , Węgry , Włochy ) , Azji ( Armenia , Chiny , Gruzja , Iran , Turcja ) , w Ameryce Północnej ( Kanada i USA ) , oraz w Republice Południowej Afryki .
