Septoria eupatorii
Septoria eupatorii Roberge ex Desm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na sadźcu konopiastym ( `` Eupatorium cannabinum '' ) i wywołujący u niego plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Występowanie .
Monofag .
Znane jest występowanie `` S. eupatorii '' w wielu krajach Europy oraz w Azji , Ameryce Północnej i Południowej .
W piśmiennictwie naukowym na terenie Polski podano 3 stanowiska .
