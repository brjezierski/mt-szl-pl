Septoria ficariae
Septoria ficariae Desm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach roślin z rodziny jaskrowatych ( `` Ranunculaceae '' ) .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Występowanie .
`` S. ficariae '' występuje w większości krajów Europy , na Nowej Zelandii , w Rosji i Chinach .
W piśmiennictwie naukowym na terenie Polski podano 7 stanowisk .
Monofag .
Opisano jego występowanie na ziarnopłonie wiosennym ( `` Ficaria verna '' ) i jaskrze płomienniku ( `` Ranunculus flammula '' ) , w Polsce tylko na ziarnopłonie wiosennym .
