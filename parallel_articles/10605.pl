Septoria geranii
Septoria geranii Roberge ex Desm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt , endobiont rozwijający się na liściach różnych gatunków bodziszków ( `` Geranium '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisali Michel Robert Roberge i Jean Baptiste Henri Joseph Desmazières w 1853 r. Synonim : `` Rhabdospora geranii '' ( Roberge ex Desm . )
Petr .
1958 .
Morfologia .
Na obydwu stronach porażonych liści powstają wielokątne , okrągławe lub elipsoidalne , ograniczone nerwacją plamy o wymiarach 2-5 ( -10 ) x 2-4 mm .
Początkowo są zielonobrązowe lub brązowe , potem czarnobrązowe i mają cienką , purpurową obwódkę oraz purpurowofioletowe halo .
W obrębie plam tworzą się pyknidia o średnicy ( 50– ) 70–180 μm .
Powstają na obydwu stronach liści i są zagłębione w jego tkankach , później bardziej widoczne stają się na górnej stronie blaszki .
Wydostają się przez pojedynczą , czasami otoczoną ciemniejszymi komórkami Ostiola o średnicy 28–50 μm .
Konidia podzielone 1–3 septami .
Są dwa ich rodzaje :
Znana jest tylko postać bezpłciowa ( anamorfa ) .
Występowanie .
Znane jest występowanie tego gatunku w licznych krajach Europy , w Azji , Ameryce Północnej , w Afryce i Australazji .
