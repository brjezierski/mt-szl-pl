Septoria leontodontis
Septoria leontodontis A.L .
Sm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt , endobiont rozwijający się na liściach brodawnika jesiennego ( `` Leontodon autumnalis '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisała Annie Lorrain Smith w 1915 r .
Morfologia .
Na obydwu stronach porażonych liści powstają bardzo małe , koliste plamki o średnicy do 1 mm , białe z brązową obwódką .
W obrębie plam na górnej tworzą się pyknidia o średnicy 100–120 μm .
Konidia podzielone 1–3 septami .
Mają wymiary 30–38 × 1–1,5 μm .
Wydostają się przez pojedyncze ostiolum o średnicy 28–50 μm .
Znana jest tylko postać bezpłciowa ( anamorfa ) .
