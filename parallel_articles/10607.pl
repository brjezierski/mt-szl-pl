Septoria lycopersici
Septoria lycopersici Pass .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Według Index Fungorum takson niepewny .
Pasożyt pomidora ( `` Solanum lycopersicum '' ) .
Grzyb mikroskopijny , endobiont rozwijający się w tkankach roślin .
Wywołuje chorobę roślin o nazwie septorioza pomidora .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisał Carlo Luigi Spegazzini w 1882 r. na pomidorze zwyczajnym w Buenos Aires w Argentynie .
Morfologia .
Na obydwu stronach porażonych liści powstają okrągławe plamy o średnicy do 5 mm .
Początkowo są zielonobrązowe lub brązowe z ciemnobrązową obwódką , potem ich środek staje się szarobrązowy lub żółtobrązowy .
Zazwyczaj są bardzo liczne i łączą się z sobą zajmując znaczną część powierzchni liścia .
Silnie porażone liście obumierają .
W obrębie plam tworzą się pyknidia o średnicy 80–160 μm .
Powstają na obydwu stronach liści , ale na górnej jest ich dużo więcej .
Tworzą się podskórnie , lub są mniej lub bardziej zanurzone w warstwie miękiszu palisadowego .
Na ich wewnętrznej ścianie na konidioforach powstają proste lub nieco wygięte , nitkowate konidia o wymiarach 150–108 ( –126 ) × 2–2,5 μm , z 3–8 przegrodami .
Wydostają się przez pojedynczą , czasami otoczoną ciemniejszymi komórkami ostiolę o średnicy 58–88 μm .
Znana jest tylko postać bezpłciowa ( anamorfa ) .
Występowanie .
Znane jest występowanie tego gatunku w licznych krajach Europy , w Azji , Ameryce Południowej , Środkowej i Północnej i w Australazji .
