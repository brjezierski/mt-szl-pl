Septoria quercicola
Septoria quercicola Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , saprotrof i pasożyt wielu gatunków dębów .
Jest rozprzestrzeniony na całym świecie , na obszarach o klimacie umiarkowanym jest pospolity .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Jak dotąd znany jest wyłącznie w postaci bezpłciowej anamorfy .
Wiadomo jednak , że teleomorfy w obrębie rodzaju `` Septoria '' należą do `` Mycosphaerella '' .
Morfologia .
Rozwija się jako endobiont w tkankach liści dębów .
Na liściach dębów powoduje powstawanie plamistości .
Plamy mają czerwono-brązową barwę .
Ich środkowa część jest ciemniejsza , często widoczne są w niej martwicze tkanki liścia .
