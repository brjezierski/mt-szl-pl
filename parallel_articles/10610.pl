Występowanie .
`` S. scabiosicola '' występuje w większości krajów Europy , w niektórych rejonach Azji ( Gruzja , Rosja ) i na Nowej Zelandii .
W Polsce jest jednym z najczęściej występujących gatunków rodzaju `` Septoria '' .
W piśmiennictwie naukowym na terenie Polski podano liczne jej stanowiska .
Opisano występowanie na wielu gatunkach z rodziny szczeciowatych .
W Polsce notowana najczęściej na świerzbnicy polnej ( `` Knautia arvensis '' ) , poza tym na driakwi wonnej ( `` Scabiosa canescens '' ) , driakwi gołębiej ( `` Scabiosa columbaria '' ) , driakwi lśniącej ( `` Scabiosa lucida '' ) , driakwi żółtej ( `` Scabiosa ochroleuca '' ) , czarcikęsie łąkowym ( `` Succisa pratensis '' ) .
