Septoria scabiosicola
Septoria scabiosicola Desm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach roślin z rodziny szczeciowatych ( `` Dipsacaceae '' ) .
Powoduje u nich plamistość liści .
