Septoria sorbi
Septoria sorbi Lasch – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt roślin z rodzaju jarząb ( `` Sorbus '' ) .
W Polsce notowany na jarząbie pospolitym ( `` Sorbus aucuparia '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Agata Wołczańska , autorka monografii rodzaju `` Septoria '' opisała go pod nazwą `` Septoria aucupariae '' Bres .
i synonimizuje go z `` Septoria sorbi '' Lasch .
Morfologia .
Endobiont rozwijający się w tkankach roślin .
Powoduje plamistość liści .
