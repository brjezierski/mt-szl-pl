Septoria tanaceti
Septoria tanaceti Niessl – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju wrotycz ( `` Tanacetum '' ) i wywołujący u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Na `` Tanacetum '' opisano jeszcze inne gatunki `` Septoria '' : `` Septoria tanaceti-macrophylli '' Bubák i `` Septoria pyrethri '' Bresadola et Krieger .
Prawdopodobnie są to synonimy .
Występowanie .
Monofag .
Znane jest występowanie `` S. tanaceti '' w niektórych krajach Europy .
W piśmiennictwie naukowym na terenie Polski podano 3 stanowiska , wszystkie na wrotyczu pospolitym ( `` Tanacetum vulgare '' ) .
