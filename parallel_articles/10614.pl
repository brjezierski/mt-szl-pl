Septoria veronicae
Septoria veronicae Roberge ex Desm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Według Index Fungorum takson niepewny .
Grzyb mikroskopijny , pasożyt , endobiont rozwijający się na liściach przetaczników ( `` Veronica '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Septoria , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisali Michel Roberge i Jean Baptiste Henri Joseph Desmazières w 1849 r. Synonim : `` Septoria veronicae '' var .
`` major '' Died .
1914 .
Morfologia .
Na obydwu stronach porażonych liści powstają małe , koliste , jasnobrązowe lub ółtobrązowe plamki z bardzo cienka , ciemnobrązową obwódką .
Mają średnicę do 3 mm .
Po uschnięciu liści plamki stają się niewidoczne i cały liść pokrywa się owocnikami .
W obrębie plam na obydwu stronach liścia tworzą się pyknidia o średnicy 66–132 μm .
Konidia proste lub nieco wygięte , o wymiarach 20-56 × 1-1,5 μm , podzielone 0–3 septami .
Mają wymiary 30–38 × 1–1,5 μm .
Wydostają się przez pojedyncze , otoczone ciemniejszymi komórkami ostiolum o średnicy 10–44 μm .
Znana jest tylko postać bezpłciowa ( anamorfa ) .
Występowanie .
Występuje w Europie ( Belgia , Bułgaria , dawna Czechosłowacja , Dania , Francja , Litwa , Łotwa , Niemcy , Polska , Rosja , Rumunia , Ukraina , Węgry , Wielka Brytania , Włochy ) , w Azji ( Chiny , Kazachstan , Rosja ) i w Ameryce Północnej ( USA , Kanada ) .
W Polsce stwierdzono występowanie `` Septoria veronica '' na przetaczniku ożankowym ( `` Veronica chamaedrys '' ) i przetaczniku górskim ( `` Veronica montana '' ) – obydwa stanowiska w Słowińskim Parku Narodowym ( 1993 i 2001 r. ) .
