Siergiej Michałkow
Siergiej Władimirowicz Michałkow , ros .
Сергей Владимирович Михалков ( ur .
w Moskwie , zm .
27 sierpnia 2009 tamże ) – rosyjski poeta i bajkopisarz .
Pochodził ze starej rosyjskiej rodziny ziemiańskiej , której korzenie sięgają XV wieku .
Michałkow był autorem wielu książek i wierszy dla dzieci , jak również twórcą tekstu hymnu ZSRR i hymnu Rosji .
W latach 1970–1992 pełnił funkcję prezesa Stowarzyszenia Pisarzy Rosyjskiej FSRR .
Był ojcem reżyserów Andrieja Michałkowa-Konczałowskiego i Nikity Michałkowa .
Pochowany na Cmentarzu Nowodziewiczym w Moskwie .
