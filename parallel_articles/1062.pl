Grzyb mikroskopijny .
Znana jest tylko forma bezpłciowa ( anamorfa ) , która jest grzybem pasożytniczym , patogenem roślin .
Powoduje więdnięcie kwiatostanów i martwicę naczyń w uprawach drzew owocowych , takich jak mango ( `` Mangifera '' ) , longan ( `` Dimocarpus longan ) '' i jagodzian rambutan ( `` Nephelium lappaceum '' ) i innych .
Występuje również w Polsce .
W. Mułenko i inni wymieniają ten gatunek pod nazwą `` Fusarium decemcellulare '' .
W Polsce zidentyfikowany został na kukurydzy ( `` Zea mays '' ) .
