Serravalle
Serravalle – zamek ( `` castello '' ) w San Marino , który jest jednym z 9 tego typu jednostek administracyjnych w kraju .
Miejscowość zlokalizowana jest w północnej części San Marino .
Serravalle to największy zamek w tym państwie .
W 2012 roku liczył mieszkańców .
Nazwa zamku pochodzi od słów : `` serra '' ( łańcuch ) i `` valle '' ( niewielka dolina ) .
