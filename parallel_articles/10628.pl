Sesto San Giovanni
Sesto San Giovanni – miasto i gmina w północnych Włoszech ( Lombardia ) .
Sesto S.G. znajduje się w aglomeracji Mediolanu .
Duży ośrodek przemysłowy kraju .
Przemysł : metalurgiczny ( hutnictwo żelaza ) , środków transportu ( samoloty , ciągniki , motocykle , wagony kolejowe ) , maszynowy , elektrotechniczny , metalowy , chemiczny , farmaceutyczny , włókienniczy , poligraficzny , papierniczy .
W mieście siedzibę ma Campari Group , jeden z czołowych światowych producentów napojów alkoholowych .
Według danych na rok 2004 gminę zamieszkiwało 75 421 osób .
