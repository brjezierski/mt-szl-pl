Seszele
Seszele ( fr. , ang .
`` Seychelles '' , sesz .
`` Sesel '' ) , oficjalnie : Republika Seszeli – państwo wyspiarskie na Oceanie Indyjskim , ok. 1600 km od wybrzeży Afryki , położone na północny wschód od Madagaskaru .
Najbliższe państwa i terytoria zamorskie to Mauritius i Reunion na południu oraz Malediwy na północnym wschodzie .
Historia .
Wyspy były pierwotnie niezamieszkane .
Od X wieku były odwiedzane przez kupców arabskich .
Na początku XVI wieku zostały odkryte przez Portugalczyków dla Europy .
Od końca XVII do początku XVIII wieku stanowiły schronienie piratów europejskich .
W połowie XVIII wieku archipelag został anektowany przez Francję .
Nazwa wysp pochodzi od nazwiska francuskiego ministra finansów na dworze króla Ludwika XV , Jeana Moreau de Sechelles .
W 1756 roku przybyli tu pierwsi osadnicy francuscy , którzy następnie sprowadzili na miejsce niewolników .
Od 1794 roku Seszele znajdowały się pod okupacją Wielkiej Brytanii .
W 1814 roku przynależność wysp została prawnie usankcjonowana przez traktat paryski .
Kultura i język francuski pozostały dominujące .
W 1903 roku stały się odrębną kolonią z gubernatorem ( wcześniej zarządzane były przez władze Mauritiusu ) .
Zniesienie niewolnictwa w Imperium Brytyjskim w 1833 roku doprowadziło do imigracji na wyspy ludności hinduskiej .
