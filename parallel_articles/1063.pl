Albuquerque
Albuquerque – miasto w Stanach Zjednoczonych , w stanie Nowy Meksyk , ośrodek administracyjny hrabstwa Bernalillo , położone nad rzeką Rio Grande , u podnóża pasma górskiego Sandia ( na wschodzie ) .
Największe miasto w Nowym Meksyku , główny ośrodek gospodarczy oraz węzeł transportowy tego stanu .
W 2020 roku miasto zamieszkane było przez 564 559 mieszkańców ; obszar metropolitalny – 916 528 .
Historia .
Założone w 1706 przez Hiszpanów na szlaku Camino Real .
Nazwa pochodzi od jednego z tytułów wicekróla Nowej Hiszpanii , Francisca Fernándeza de la Cueva , księcia Alburquerque .
Obecnie największe miasto i główny ośrodek przemysłowy stanu .
Przemysł elektroniczny , odzieżowy , metalowy , chemiczny i drzewny ; rzemiosło indiańskie .
Węzeł komunikacyjny ; 2 uniwersytety ; ośrodek turystyczny .
W okresie II wojny światowej w tym mieście była leczona żona Richarda Feynmana ( późniejszego laureata Nagrody Nobla ) , która zmarła w 1945 .
W tym czasie Feynman uczestniczył w Los Alamos w pracach nad bombą atomową .
