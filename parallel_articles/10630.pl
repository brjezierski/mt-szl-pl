Exserohilum turcicum ( Pass . )
K.J .
Leonard & amp ; Suggs – gatunek grzybów z klasy `` Dothideomycetes '' .
Pasożyt wywołujący chorobę o nazwie helmintosporioza liści kukurydzy .
Atakuje ona sorgo i kukurydzę .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Exserohilum '' , `` Pleosporaceae '' , `` Pleosporales '' , `` Pleosporomycetidae '' , `` Dothideomycetes '' , `` Pezizomycotina '' , `` Ascomycota '' , `` Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1876 A. Passo , nadając mu nazwę `` Helminthosporium turcicum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w roku 1974 K.J .
Leonard i E.G .
Suggs .
