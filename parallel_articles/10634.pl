Sewilla
Sewilla ( `` hiszp .
Sevilla '' ) – miasto w południowej Hiszpanii , stolica regionu Andaluzja .
Jest czwartym co do wielkości miastem Hiszpanii , z liczbą ludności na poziomie 684 234 mieszkańców .
Metropolia liczy 1 316 591 mieszkańców .
Duży port nad rzeką Gwadalkiwir , w XVII wieku najludniejsze miasto Hiszpanii , znane z największej katedry świata , pałacu i XVIII w. archiwum wpisanych na listę UNESCO .
W rankingu opublikowanym przez `` Globalization and World Cities Research Network '' , Sewilla została sklasyfikowana w IV kategorii ( `` sufficiency level cities '' ) miast o znaczeniu globalnym .
