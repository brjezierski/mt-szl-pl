Σεβαστούπολη ) – miasto w południowej części Półwyspu Krymskiego , przy Zatoce Sewastopolskiej .
`` De iure '' należy do Ukrainy , zaś `` de facto '' od 2014 roku stanowi część Rosji jako jedno z jej miast wydzielonych .
Według ostatniego ukraińskiego spisu powszechnego ( 2014 ) miasto liczyło 385 998 osób .
Według rosyjskiego spisu powszechnego miasto liczy obecnie ( 2021 ) 513 149 osób .
Od 1954 roku razem z resztą Półwyspu Krymskiego traktowany jako część Ukraińskiej Socjalistycznej Republiki Radzieckiej .
Po przekształceniu Ukraińskiej SRR w niepodległą Ukrainę w 1991 roku znalazł się pod bezpośrednią kontrolą władz tego państwa jako miasto wydzielone , nie wchodzące w skład republiki autonomicznej obejmującej Półwysep Krymski .
W marcu 2014 roku rada miasta jednostronnie ogłosiła , że Sewastopol będzie częścią Federacji Rosyjskiej .
Niedługo później zarówno tereny miasta wydzielonego Sewastopol , jak i Autonomicznej Republiki Krymu przyłączono do Rosji , co było następstwem referendum , w którym głosujący przytłaczającą większością głosów opowiedzieli się za przynależnością krymskich ziem do Federacji Rosyjskiej .
Po przyłączeniu do Rosji Sewastopol zachował status miasta wydzielonego , jednak jako część Rosji nie jest uznawany przez społeczność międzynarodową , która uważa Sewastopol i cały Krym za część Ukrainy .
Miasto stanowi wielki port wojenny na Morzu Czarnym , bazę rosyjskiej Floty Czarnomorskiej po rozpadzie ZSRR .
Położenie .
Sewastopol znajduje się na południowo-zachodnim krańcu Półwyspu Krymskiego na cyplu zwanym Półwyspem Heraklesa na wybrzeżu Morza Czarnego .
Samo miasto jest skoncentrowana głównie w zachodniej części regionu na wybrzeżu Zatoki Sewastopolskiej .
We wschodniej części miasta znajduje się Bałakława , która w 1957 roku została włączona do Sewastopola .
