Shaggy
Shaggy , właśc .
Orville Richard Burrell ( ur .
22 października 1968 w Kingston ) – jamajski wokalista reggae , dancehall i rap .
Wczesne lata .
Urodził się w Kingston na Jamajce .
W dzieciństwie wraz z rodziną wyemigrował do Stanów Zjednoczonych i zamieszkał w Nowym Jorku .
W 1987 wziął lekcje śpiewu i został odkryty rok później , śpiewając na ulicy z przyjaciółmi .
W 1988 zaciągnął się do piechoty morskiej , służbę wojskową odbywał podczas pierwszej wojny w Zatoce Perskiej .
Po powrocie z Iraku zdecydował się na rozpoczęcie kariery muzycznej .
Kariera .
Debiutował w 1993 roku aranżacją utworu zespołu ska Folkes Brothers – „ Oh Carolina ” .
Przybrał pseudonim artystyczny na cześć bohatera kreskówki Scooby Doo – Kudłatego ( ang .
`` Shaggy '' ) .
W następnych latach zanotował sporo sukcesów ( m.in .
przeboje „ Boombastic ” i „ It Was n't Me ” ) .
W 2005 roku nagrał album studyjny `` Clothes Drop '' .
Natomiast w 2007 roku wziął udział w dyskusyjnym programie stacji MTV – `` The Naked Truth About Boys And Girls '' .
W 2008 roku ułożył specjalnie piosenki na Mistrzostwa Europy w Piłce Nożnej 2008 – „ Like a Superstar ” i „ Feel the Rush ” .
Shaggy podejmuje także działalność charytatywną .
3 stycznia 2009 zorganizował koncert charytatywny w Kingston z którego dochód wspomoże jeden ze szpitali dziecięcych na Jamajce .
W trakcie koncertu wykonał utwór „ Save a Life ” który powstał na tę okazję a w jego nagraniu wzięli udział tacy artyści jak : Chris Martin , Sean Paul , Elephant Man , Gramps Morgan , Tessane Chin , Luciano , D Major , DLYNX , Freddy McGregor , Marcia Griffiths , Etana i Daville .
