Shamrock ( filipiński zespół muzyczny )
Shamrock – filipiński zespół muzyczny grający rock alternatywny .
Został założony w 2002 roku .
W skład formacji wchodzą : wokalista Marc Tupaz , perkusista Harald Huyssen , basista Sam Santos oraz gitarzysta Nico Capistrano .
Wylansowali takie utwory jak „ Alipin ” , „ Nandito Lang Ako Haplos ” i „ Pagkakataon ” .
Grupa nagrała podkład muzyczny do czołówek wielu seriali telewizyjnych , w tym `` Captain Barbel '' , `` Jumong '' i `` Jewel in the Palace '' .
