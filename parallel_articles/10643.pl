Historia .
Polski oddział firmy Sharp został otwarty w sierpniu 2000 roku i pełni funkcję centrali na Europę Środkowo-Wschodnią ( CEE ) .
Od stycznia 2007 do końca 2014 roku Sharp posiadała też fabrykę telewizorów LCD w Ostaszewie koło Torunia .
W 2014 roku słowacka spółka Universal Media Corporation i Sharp podpisały umowę , w ramach której UMC przejęła 100 % udziałów w zakładzie produkującym telewizory .
UMC otrzymała prawo do korzystania na terenie Europy z marki Sharp ( projektowanie , produkcja i dystrybucja ) , zarówno jeśli chodzi o urządzenia audio , jak i video .
Z kolei sprzedaż i marketing urządzeń gospodarstwa domowego w Europie ( wyłączając niektóre kraje ) zostały przejęte przez turecką firmę Vestel .
Działalność na polskim rynku .
Obecnie Sharp wspiera działalność swoich klientów w zakresie zarządzania dokumentami , procesami skanowania , archiwizacji i obiegu dokumentów , a także Managed Content Services oraz integracji procesów IT .
Sharp jest producentem sprzętu drukującego dla środowisk biurowych oraz poligrafii – urządzeń wielofunkcyjnych oraz cyfrowych systemów produkcyjnych .
Przedsiębiorstwo działa również w obszarach Visual Solutions , paneli słonecznych oraz systemów oświetleniowych LED .
