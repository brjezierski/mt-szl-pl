Shepperton
Shepperton – miasto w Anglii , w hrabstwie Surrey , w dystrykcie Spelthorne .
Leży 26 km na południowy zachód od centrum Londynu .
Miasto liczy 10 796 mieszkańców .
W mieście znajduje się wytwórnia filmów Shepperton Studios , w której powstały m.in .
