Alcalá la Real
Alcalá la Real – miasto w południowej Hiszpanii w regionie Andaluzji , leży w odległości 71 km od stolicy prowincji Jaén i 53 km od Grenady .
Historia .
Alcalá la Real istnieje od czasów rzymskich .
Od początku VIII wieku było to obronne miasto mauretańskie o dużym znaczeniu strategicznym , które straciło dopiero po upadku Grenady w 1492 r .
