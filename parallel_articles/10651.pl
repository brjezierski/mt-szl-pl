Shimada
– miasto w Japonii , w prefekturze Shizuoka ( środkowe Honsiu ) .
Położenie .
Miasto leży w środkowej części prefektury Shizuoka , nad brzegami rzeki Ōi ( Ōi-gawa ) .
W jego pobliżu znajdują się miasta : Shizuoka , Hamamatsu , Fujieda , Makinohara , Kakegawa oraz Kikugawa .
Historia .
W 1997 r. most Hōrai , łączący brzegi rzeki Ōi , został wpisany do Księgi rekordów Guinnessa jako najdłuższy pieszy , drewniany most na świecie .
