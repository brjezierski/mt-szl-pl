Shinzō Abe
– japoński polityk , premier Japonii w latach 2006–2007 i 2012–2020 .
Przewodniczący Partii Liberalno-Demokratycznej w latach 2012–2020 .
Członek Nippon Kaigi .
Zmarł w wyniku postrzelenia przez zamachowca .
Życiorys .
Był synem byłego ministra spraw zagranicznych Shintarō Abego i wnukiem premiera w latach 1957–1960 , Nobusuke Kishiego .
Ukończył nauki polityczne na Uniwersytecie Seikei ( 1977 ) w Musashino .
Przez 3 lata pracował w koncernie Kobe Steel , w 1982 został sekretarzem swojego ojca .
W latach 2005–2006 był szefem gabinetu premiera .
We wrześniu 2006 został premierem .
Uważano go za politycznego „ jastrzębia ” .
Jego poprzednikiem i mentorem był Jun ’ ichirō Koizumi .
Abe zapowiadał zmianę pacyfistycznej konstytucji Japonii i zwiększenie roli polityczno-militarnej państwa , jak to wynika z potencjału gospodarczego i demograficznego .
Pomimo że uważa się go za nacjonalistę , zaniechał wówczas wizyt w chramie Yasukuni , by poprawić stosunki z Chinami i Koreą Południową .
W tym celu odbył też spotkania z przywódcami tych państw .
Forsował program promowania patriotyzmu i zwiększenia dyscypliny w szkołach .
Latem 2007 zaproponował utworzenie czterostronnego „ łuku wolności i dobrobytu ” ( Indie , Japonia , USA , Australia ) w celu zrównoważenia rosnącej potęgi Chin .
12 września 2007 po wyjściu na jaw wielu afer finansowych , związanych z ministrami wchodzącymi w skład jego rządu , podał się do dymisji ze stanowiska premiera Japonii .
We wrześniu 2012 został wybrany przewodniczącym Partii Liberalno-Demokratycznej ( PLD ) .
W przedterminowych wyborach parlamentarnych , które odbyły się 16 grudnia 2012 po rozwiązaniu Izby Reprezentantów ( niższej izby parlamentu Japonii ) przez premiera Yoshihiko Nodę ( z centrolewicowej Partii Demokratycznej ) , PLD uzyskała większość parlamentarną , a Shinzō Abe został desygnowany na premiera i 26 grudnia 2012 zaprzysiężony .
Wprowadził politykę monetarną polegającą na zwiększonej radykalnie emisji pieniądza w porozumieniu z nowo mianowanym szefem banku centralnego Haruhiko Kurodą zmierzającą do ożywienia gospodarczego ( w połączeniu z innymi działaniami zwaną abenomiką ) .
26 grudnia 2013 roku premier Abe niespodziewanie przybył z oficjalną wizytą do chramu Yasukuni , co natychmiast spowodowało protesty Chin i Korei Południowej .
Negatywnie do wizyty odniosły się również Stany Zjednoczone .
W sierpniu 2020 roku zrezygnował ze stanowiska premiera z powodów zdrowotnych .
8 lipca 2022 , podczas przemówienia na wiecu wyborczym w Narze ( sam nie kandydował , ale wspierał kampanię rządzącej Partii Liberalno-Demokratycznej ) , został postrzelony i zmarł w wyniku odniesionych obrażeń .
