Shipston-on-Stour
Shipston-on-Stour lub Shipston on Stour - miasto i civil parish w Wielkiej Brytanii , w Anglii , w hrabstwie Warwickshire , w dystrykcie Stratford-on-Avon .
W 2011 roku civil parish liczyła 5038 mieszkańców .
Shipston jest wspomniana w Domesday Book ( 1086 ) jako `` Scepwestun '' .
