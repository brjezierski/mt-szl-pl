Shiyan
Shiyan ( ) – miasto o statusie prefektury miejskiej w środkowych Chinach , w prowincji Hubei , w dolinie rzeki Han Shui .
W 2010 roku liczba mieszkańców miasta wynosiła 426 017 .
Prefektura miejska w 1999 roku liczyła 3 411 520 mieszkańców .
Ośrodek produkcji samochodów ciężarowych i przemysłu spożywczego .
