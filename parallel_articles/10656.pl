Szkodra
Szkodra ( lub ; / ; ; ) – miasto w północno-zachodniej Albanii , położone nad rzeką Buna i Jeziorem Szkoderskim .
Największy ośrodek gospodarczy i kulturalny w północnej części Albanii .
Liczba mieszkańców wynosi około 86 tys .
( 2003 ) .
Przemysł .
Duże zakłady przewodów i kabli miedzianych ( produkujące na eksport ) , przemysł maszynowy , materiałów budowlanych , spożywczy ( zwłaszcza tytoniowy oraz owocowo-warzywny ) , włókienniczy ( bawełniany i jedwabniczy ) , drzewno-papierniczy .
Ważny węzeł drogowy , połączenie kolejowe z Czarnogórą , port lotniczy .
