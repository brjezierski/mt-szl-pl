Powstało w roku 1835 , wówczas nazywało się Shreve 's .
W latach 1863-1865 było stolicą stanu i jednym z ostatnich bastionów oporu konfederatów w Luizjanie .
Miasto było ośrodkiem handlu bawełną .
Prawa miejskie otrzymało w 1878 roku .
Gwałtowny rozwój od roku 1906 , kiedy odkryto ropę naftową nad jeziorem Caddo Lake .
Oprócz przemysłu petrochemicznego ( rafineria ) , dominuje przemysł maszynowy , metalowy , spożywczy , drzewny , chemiczny i szklarski .
