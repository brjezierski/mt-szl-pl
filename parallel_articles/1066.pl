Zabytki .
Najważniejszym zabytkiem miejscowości jest dawne opactwo cystersów z największym w Portugalii kościołem Santa Maria de Alcobaça .
Od 1989 r. zespół ten znajduje się na liście światowego dziedzictwa UNESCO .
