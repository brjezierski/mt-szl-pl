Alcobaça
Alcobaça ( czyt .
`` alkubạsa '' ) – miejscowość w Portugalii leżąca w dystrykcie Leiria , w regionie Centrum w podregionie Oeste .
Miejscowość jest siedzibą gminy o tej samej nazwie .
