Język syberyjskotatarski
Język syberyjskotatarski ( syb .
Себер татар тел ) – język Tatarów syberyjskich , jest to jeden z języków tureckich używanych na zachodniej Syberii .
Klasyfikacja językowa jest kwestią dyskusji .
Język syberyjskotatarski został wpisany przez UNESCO na listę języków zagrożonych wymarciem .
Jest jednym z zagrożonych języków Federacji Rosyjskiej .
