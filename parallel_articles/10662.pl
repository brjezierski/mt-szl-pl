Sybin
Sybin ( , , ) – miasto w środkowej Rumunii , w Siedmiogrodzie , stolica okręgu Sybin .
Liczy 154 892 mieszkańców .
Leży nad rzeką Cibin , dopływem Aluty .
Historia .
Sybin został założony w roku 1190 przez saskich kolonistów na terenie ówczesnego Królestwa Węgier .
Najprawdopodobniej zostało zbudowane w pobliżu rzymskiego posterunku , Caedonii , opuszczonego w czasach przybycia Sasów .
W XIV wieku Sybin był już ważnym ośrodkiem handlu .
W 1376 roku rzemieślnicy byli podzieleni na 19 cechów .
Miasto było także najważniejszym z siedmiu miast , które dały najpierw niemiecką – `` Siebenbürgen '' – a następnie polską – `` Siedmiogród '' – nazwę Transylwanii .
Znajdował się tu także `` Universitas Saxorum '' , zgromadzenie Niemców w Siedmiogrodzie .
Zwyczajowo w XVII wieku opisywano Sybin jako najbardziej wysunięte na wschód miasto kultury zachodnioeuropejskiej ; było także ważnym skrzyżowaniem szlaków pocztowych biegnących na wschód – do Turcji i dalej do Persji , Chin .
