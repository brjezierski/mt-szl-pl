Sidney Wood
Sidney Burr Wood Jr. ( ur .
1 listopada 1911 w Black Rock , zm .
10 stycznia 2009 w Palm Beach ) – amerykański tenisista , zwycięzca Wimbledonu w grze pojedynczej , reprezentant w Pucharze Davisa .
Wood był wynalazcą nawierzchni tenisowej `` Supreme Court '' , powszechnie stosowanej w imprezach halowych .
Kariera tenisowa .
W turnieju wimbledońskim praworęczny Amerykanin debiutował jako 15-latek w 1927 roku .
Największy sukces w karierze odniósł w 1931 roku , wygrywając grę pojedynczą na Wimbledonie .
Do meczu finałowego nie doszło , ponieważ jego przeciwnik – Frank Shields – skręcił nogę w czasie meczu mikstowego i został wycofany z finału singla .
Był to zarazem jedyny walkower w finale Wimbledonu w historii .
W 1935 roku Wood awansował do finału na mistrzostwach USA ( obecnie US Open ) , gdzie poniósł porażkę z Wilmerem Allisonem .
Wood był również finalistą tego turnieju w grze podwójnej w 1942 roku , występując w parze z Tedem Schroederem .
