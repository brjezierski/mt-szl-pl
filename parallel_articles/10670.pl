Siena
Siena – miasto i gmina we Włoszech , w Toskanii , w prowincji Siena .
Ważne centrum turystyczne .
Według danych na 30 czerwca 2016 gminę zamieszkiwały 53 853 osoby .
Historia .
Według legendy Sienę założyli synowie Remusa – Senio i Aschio , którzy uciekając przed Romulusem schronili się w górach i wznieśli zamek `` Senio '' .
