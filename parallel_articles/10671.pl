Serce
Serce ( , ) – centralny narząd układu krwionośnego strunowców , stawonogów , pazurnic , mięczaków i ramienionogów .
Zbudowany jest z tkanki mięśniowej poprzecznie prążkowanej typu sercowego .
Zazwyczaj narząd ten otoczony jest osierdziem ( `` pericardium '' ) .
Stawonogi .
Serce stawonogów nawiązuje do naczynia grzbietowego pierścienic .
W pierwotniejszych taksonach skorupiaków serce ciągnie się przez cały tułów i odwłok , a ostia obecne są po parze w każdym segmencie .
U wyżej uorganizowanych grup serce jest krótsze , a ostiów może być tylko para .
Ostia wyposażone są w zastawki zapobiegające cofaniu się krwi do zatoki okołosercowej przy skurczu .
Wskutek skurczu krew płynie przez aortę do tętnic , z których wylewa się do hemocelu .
Do serca dostaje się natomiast z zatoki okołosercowej .
Obecne mogą być także żyły .
