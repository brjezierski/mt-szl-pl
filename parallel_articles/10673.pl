Sierre ( stacja kolejowa )
Sierre ( fra : `` Gare de Sierre '' ) – stacja kolejowa w Sierre , w kantonie Valais , w Szwajcarii .
Stacja znajduje się na trasie Kolei Simplońskiej .
Historia .
Stacja została otwarta w 1873 roku przez Compagnie de la Ligne d'Italie .
Sama linia kolejowa została otwarta wcześniej 15 kwietnia 1868 .
W 1996 stacja została zmodernizowana i rozbudowana .
