Sighnaghi
Sighnaghi ( gruz .
: სიღნაღი ) – miasto w regionie Kachetia we wschodniej Gruzji , administracyjne centrum prowincji Sighnaghi .
Jest jednym z najmniejszym miast w kraju , jego populacja w 2014 r. wynosiła 1485 mieszkańców .
Gospodarka Sighnaghi opiera się głównie na produkcji wina i tradycyjnych dywanów .
Miasto i jego okolica znana jest z pięknych krajobrazów oraz zabytków .
Po rekonstrukcji miasta stało się ono ważnym ośrodkiem turystycznym Gruzji .
Historia .
Aktualne terytorium miasta zostało zasiedlone w okresie paleolitu i było znane jako Hereti w średniowieczu i jako Kiziqi od XV wieku .
Pierwsza wzmianka o Sighnaghi ( w języku tureckim oznacza `` fort '' ) jako osadzie została zarejestrowana w późnym XVIII wieku .
W 1762 r. , Herakliusz II zlecił wykonanie planu miasta oraz budowę fortecy , by uchronić osadę przed atakami Dagestańczyków .
Według spisu ludności z 1770 r. Sighnaghi zamieszkiwało 100 rodzin , głównie trudniących się handlem i rzemiosłem .
