Sigmaringen
Sigmaringen – miasto powiatowe w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Tybinga , w regionie Bodensee-Oberschwaben , w powiecie Sigmaringen , siedziba związku gmin Mengen .
Leży nad Dunajem , ok. 80 km na południe od Stuttgartu .
Do XIX wieku stolica księstwa Hohenzollern-Sigmaringen , w którym panowała katolicka gałąź rodu Hohenzollernów , późniejszych królów Rumunii .
Współpraca .
