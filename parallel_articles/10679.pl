Sigmund Freud
Sigmund Freud ( ur .
jako Sigismund Schlomo Freud , 6 maja 1856 w Příborze , zm .
23 września 1939 w Londynie ) – austriacki lekarz żydowskiego pochodzenia , neurolog , twórca psychoanalizy .
Od 1938 mieszkał w Wielkiej Brytanii .
Mimo że Freud nie był filozofem w ścisłym tego słowa znaczeniu , jego koncepcje funkcjonowania umysłu ludzkiego , odrzucające racjonalność ludzkich wyborów i zachowań na rzecz czynników irracjonalnych i emocjonalnych , wywarły ogromny wpływ na filozofów , uczonych i artystów przełomu XIX i XX wieku .
Ma też ogromny , wciąż żywy , wpływ na nauki o kulturze , religioznawstwo i inne nauki społeczne .
