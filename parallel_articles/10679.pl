Jakob w dniu ślubu miał 40 lat , Amalie 20 .
Ich dziecko , Sigismund Szlomo , przyszło na świat w czepku ( co zostało uznane za dobrą wróżbę ) 6 maja 1856 roku , 9 miesięcy po ślubie , w wynajmowanym mieszkaniu przy ulicy Schlossergasse 117 we Freibergu ( obecnie czeski Příbor ) .
Jakob miał wówczas dwóch dorosłych już synów z poprzedniego małżeństwa : Emanuela ( 1833–1914 ) i Philippa ( 1836–1911 ) .
Rodzice Freuda byli Żydami , ale nie przywiązywali dużej wagi do religii .
Ich ślub odbył się według rytuału reformowanego , święta religijne obchodzono jak święta rodzinne ; z czasem zaprzestano wszelkich praktyk religijnych .
Freud pisał , że duża różnica wieku między rodzicami może mieć wpływ na geniusz potomków ( jak w wypadku jego ulubionego poety Goethego ) .
Przed urodzeniem Freuda wieśniaczka przepowiedziała matce – Amalie , iż urodzi wielkiego człowieka .
Amalie zmarła w 1930 roku w wieku 95 lat .
