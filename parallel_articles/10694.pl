Simo Häyhä
Simo Häyhä , ps .
„ Biała śmierć ” , fin .
„ Valkoinen Kuolema ” , ros .
„ Белая смерть ” ( ur .
17 grudnia 1905 w Rautjärvi , zm .
1 kwietnia 2002 w Haminie ) – fiński strzelec wyborowy , najskuteczniejszy snajper w historii wojen .
Życiorys .
W 1925 wstąpił do armii fińskiej .
W czasie wojny zimowej ( 1939–1940 ) służył jako snajper .
W ekstremalnych warunkach ( temperatura od –20 do –40 °C ) , ubrany w biały ubiór ochronny , zabił z broni snajperskiej 505 żołnierzy Armii Czerwonej .
Używał fińskiego wariantu karabinu wyborowego Mosin M28 , bądź też M28/30 „ Pystykorva ” z muszką i szczerbinką zamiast celownika optycznego .
Uzasadniał to tym , że przy używaniu celownika optycznego snajper musi podnieść głowę wyżej , a poza tym możliwość wystąpienia odbić światła na soczewkach celownika zwiększa ryzyko wykrycia strzelca .
Używał też pistoletu maszynowego Suomi oraz pistoletu Lahti L-35 , z których zabił jeszcze ponad 200 nieprzyjaciół , co daje mu w sumie ponad 705 trafień śmiertelnych .
Do walki zawsze ubierał się całkowicie na biało .
Jeżeli zamierzał strzelać długo z jednej kryjówki , polewał wodą śnieg , by podmuch wystrzału nie uniósł zdradzającego pozycję śnieżnego pyłu .
Ponieważ w tak niskich temperaturach w wydychanym powietrzu skraplają się ogromne ilości pary wodnej , wkładał do ust śnieg ( ten prowizoryczny filtr zatrzymywał parę wodną ) .
Jego akcje wpływały demoralizująco na oddziały ZSRR .
Sowieci wielokrotnie i bezskutecznie próbowali go zlikwidować , przeprowadzając naloty bombowe i używając ciężkiej artylerii oraz wysyłając swoich snajperów .
Najlepszym rezultatem , jaki osiągnęli , było zniszczenie płaszcza Simo , w który zaplątał się odłamek szrapnela .
Pewnego dnia parę metrów od niego spadł radziecki pocisk .
Simo cudem przeżył , odnosząc tylko lekkie rany , które nie zagrażały jego życiu .
6 marca 1940 został postrzelony w szczękę .
Pocisk odbił się po uderzeniu i opuścił jego głowę .
Zanim stracił przytomność , zdążył jeszcze zastrzelić radzieckiego żołnierza , który go zranił .
Jeden z żołnierzy , którzy odnosili go do punktu opatrunkowego , opowiadał , iż „ brakowało mu połowy głowy ” .
Odzyskał przytomność 13 marca , w dzień zawarcia pokoju .
Wkrótce po wojnie marszałek Carl Gustaf Mannerheim awansował go z rangi kaprala do stopnia podporucznika .
Nikt inny w historii Finlandii nie został awansowany tak wysoko w tak krótkim czasie .
Minęło kilka lat , zanim całkowicie wyzdrowiał z odniesionych ran .
Sowiecka kula zniszczyła jego szczękę i rozerwała policzek .
Po wojnie zajął się polowaniem i hodowlą psów .
Swoje ostatnie lata spędził w małej wiosce Ruokolahti znajdującej się w południowo-wschodniej Finlandii ( Karelia Południowa ) , leżącej 15 km od rosyjskiej granicy .
Kiedy w 1998 spytano go o sekret jego celności , odpowiedział : `` praktyka '' , zaś swój rekord ponad 505 zabitych wrogów skomentował słowami : `` Robiłem to , co mi kazano – najlepiej , jak mogłem '' .
