Ciemnoboczniak pachnący
Ciemnoboczniak pachnący ( `` Simocybe sumptuosa '' ( P.D .
Orton ) Singer ) – gatunek grzybów należący do rodziny ciżmówkowatych ( `` Crepidotaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Simocybe , Crepidotaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go Peter Darbishire Orton w 1960 r. , nadając mu nazwę `` Naucoria sumptuosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Rolf Singer w 1961 r .
