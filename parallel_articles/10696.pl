Simon Ammann
Simon Ammann ( ur .
25 czerwca 1981 w Grabs ) – szwajcarski skoczek narciarski , reprezentant klubu SSC Toggenburg , a wcześniej RG Churfirsten .
Jeden z najbardziej utytułowanych skoczków narciarskich w historii tej dyscypliny .
Czterokrotny indywidualny mistrz olimpijski ( po dwa tytuły w 2002 i 2010 roku ) .
Czterokrotny medalista mistrzostw świata w narciarstwie klasycznym – mistrz i wicemistrz z 2007 roku oraz brązowy medalista w 2009 i 2011 roku .
Mistrz świata w lotach narciarskich w 2010 roku .
Zdobywca Kryształowej Kuli za zwycięstwo w Pucharze Świata 2009/2010 , drugi zawodnik sezonów 2008/2009 i 2010/2011 oraz trzeci sezonu 2006/2007 w Pucharze Świata w skokach narciarskich .
Zdobywca Pucharu KOP w sezonie 2009/2010 .
Trzeci zawodnik Pucharu Świata w lotach narciarskich w sezonach 2006/2007 ( nieoficjalnie ) , 2008/2009 , 2009/2010 i 2011/2012 .
Drugi zawodnik 57. i 59. edycji Turnieju Czterech Skoczni oraz trzeci zawodnik 55. i 62 .
Turnieju Czterech Skoczni .
Zwycięzca Turnieju Nordyckiego 2010 oraz trzeci zawodnik tego turnieju w latach 2004 , 2007 i 2009 .
Zwycięzca 23 konkursów zaliczanych do klasyfikacji Pucharu Świata .
Zwycięzca Letniego Grand Prix 2009 i zaliczanego do niego Turnieju Czterech Narodów .
Drugi zawodnik Letniego Grand Prix 2008 .
Sześciokrotny triumfator konkursów Letniego Grand Prix .
Rekordzista Szwajcarii w długości skoku narciarskiego .
