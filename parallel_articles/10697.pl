Simon Pegg
Simon Pegg , właśc .
Simon John Beckingham ( ur .
14 lutego 1970 w Gloucester w Anglii ) – brytyjski aktor , komik , scenarzysta , producent filmowy i reżyser .
Życiorys .
Wystąpił m.in .
w serialu komediowym `` Spaced '' ( nagrodzonym BAFTA ) oraz filmach – `` Wysyp żywych trupów '' , `` Hot Fuzz – Ostre psy '' i `` To już jest koniec '' .
Serial oraz wszystkie trzy filmy reżyserowane były przez przyjaciela komika , brytyjskiego reżysera filmowego Edgara Wrighta .
Wspólnie ze swoim najlepszym przyjacielem Nickiem Frostem napisał oraz wystąpił w komedii sci-fi – `` Paul '' .
Pegg współtworzył również scenariusz do `` Gazu , mięczaku , gazu ! ''
razem z popularnym irlandzkim komikiem Dylanem Moranem .
