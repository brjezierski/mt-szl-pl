Simon Tahamata
Simon Melkianus Tahamata ( ur .
26 maja 1956 w Vught ) – były holenderski piłkarz , grający na pozycji napastnika .
Zaczynał karierę w małym mieście Tiel , w klubie o nazwie Theole .
Potem trafił do znanej szkółki piłkarskiej Ajaksu Amsterdam .
Tahamata do pierwszego zespołu został włączony w sezonie 1976–1977 .
W Eredivisie zadebiutował 24 października 1976 roku w wygranym 7-0 meczu z zespołem FC Utrecht .
Ten lewoskrzydłowy już w następnym sezonie był graczem pierwszej jedenastki i przez 3 lata gry w Ajaksie był jednym z objawień .
Wtedy to w Ajaksie brylowali Ruud Geels , Frank Arnesen czy Søren Lerby .
Tahamata 2 razy był Mistrzem Holandii ( 1979 i 1980 ) oraz 1 raz zdobył Puchar Holandii ( 1979 ) .
W 1980 roku Tahamata przystał na transfer do belgijskiego Standardu Liège .
Przez 4 sezony w 129 meczach zdobył 40 bramek , a Standard w tym czasie zdobył 2 tytuły mistrzowskie w latach 1982 i 1983 .
W 1984 Tahamata wrócił do Holandii .
Tym razem zasilił szeregi Feyenoordu .
Tam grał 3 sezony jednak nie zdobywając żadnych trofeów .
W 1987 roku powrócił tym razem do Belgii i trafił do drużyny Beerschotu Antwerpia , a w 1990 roku był zawodnikiem Germinalu Ekeren , w którym grał do 1996 roku , kończąc piłkarską karierę w wieku 40 lat .
Ogółem w belgijskiej ekstraklasie rozegrał aż 408 meczów , zdobywając 71 goli .
W reprezentacji Holandii Tahamata zadebiutował 22 maja 1979 roku w zremisowanym 0-0 meczu z Argentyną .
Jednak nie zagrał nigdy w wielkiej imprezie .
Ogółem wystąpił w niej 22 razy i zdobył 2 bramki .
