Simson
Simson – były niemiecki producent motocykli i motorowerów .
Działalność firmy rozpoczęli bracia Simson w roku 1856 od produkcji broni .
Dziś z ich nazwiskiem głównie są kojarzone motorowery , które zyskały dużą popularność w Niemczech wschodnich ( NRD ) , Polsce oraz innych krajach sąsiednich , zwłaszcza w krajach bloku wschodniego .
Najbardziej popularnymi w Polsce są modele Simson S51 oraz skuter Simson SR50 .
Sukcesy na rynku polskim przypadają na przełom lat 80. i 90 .
W tym też okresie ( lata 1988–1989 ) fabryka w Suhl wyprodukowała najwięcej jednośladów .
Do dziś motorowery Simson , choć w większości liczą ponad 25 lat , wciąż cieszą się uznaniem , ze względu na dużą bezawaryjność , ale także łatwy dostęp do części zamiennych , jak również prostotę naprawy .
Jesienią roku 2002 , po wielu latach niepowodzeń w odmienionej gospodarczo i politycznie nowej rzeczywistości rynkowej ( transformacja ustrojowa ) , produkcja została ostatecznie wstrzymana .
Upadłość zakładów ogłoszono 1 lutego 2003 .
Historia .
Pierwszym motorowerem produkowanym w Suhl od 1956 roku był Simson SR1 od 1958 roku zastąpiony przez model SR2 .
