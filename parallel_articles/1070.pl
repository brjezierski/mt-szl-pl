Aleksander Doba
Aleksander Ludwik Doba ( ur .
9 września 1946 w Swarzędzu , zm .
22 lutego 2021 na Kilimandżaro w Tanzanii ) – polski inżynier , podróżnik i kajakarz .
W plebiscycie „ National Geographic ” zdobył tytuł „ Podróżnika Roku 2015 ” .
Jako pierwszy człowiek w historii samotnie przepłynął kajakiem Ocean Atlantycki z kontynentu na kontynent ( z Afryki do Ameryki Południowej ) , wyłącznie dzięki sile mięśni .
Opłynął kajakiem Morze Bałtyckie i Bajkał .
Również jako pierwszy przepłynął wzdłuż polskiej morskiej granicy z Polic do Elbląga oraz Polskę po przekątnej z Przemyśla do Świnoujścia .
Dwukrotny złoty , srebrny i brązowy medalista Otwartych Akademickich Mistrzostw Polski w kajakarstwie górskim .
24 lutego 2015 został honorowym obywatelem Swarzędza .
Zmarł tuż po zdobyciu najwyższego szczytu Afryki – Uhuru na wulkanie Kibo ( Kilimandżaro ) .
Życiorys .
Syn Wincentego i Eugenii .
Dzieciństwo spędził w Swarzędzu .
Ukończył Politechnikę Poznańską z tytułem inżyniera mechanika .
