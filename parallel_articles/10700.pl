Sinaia ( stacja kolejowa )
Sinaia ( rum : `` Gara Sinaia '' ) – stacja kolejowa w Sinaia , w Okręgu Prahova , w Rumunii .
Stacja jest obsługiwana przez pociągi Căile Ferate Române ( CFR ) .
Znajduje się na ważnej magistrali kolejowej nr 300 Bukareszt – Oradea .
Budynek dworca jest obiektem zabytkowym .
W Budynku dworca funkcjonuje poczekalnia , posterunek policji , biuro podróży , punkty usługowe , toaleta .
Przed budynkiem dworca znajduje się postój taksówek .
