Został założony przez Brytyjczyków w 1819 i uzyskał niepodległość 9 sierpnia 1965 roku .
Jego nazwa pochodzi od dwóch sanskryckich słów : `` simha '' ( lew ) i `` pura '' ( miasto ) , stąd niekiedy stosowana nazwa `` Miasto Lwa '' .
Wizerunek posągu `` Merlion '' jest znanym symbolem Singapuru , używanym do 1997 jako logo przez tutejszą izbę turystyki .
Singapur jest siedzibą APEC i należy do takich organizacji jak : Azjatycki Bank Rozwoju , Międzynarodowa Agencja Energii Atomowej , Międzynarodowa Organizacja Pracy , Międzynarodowy Bank Odbudowy i Rozwoju , Międzynarodowy Fundusz Walutowy , Organizacja Narodów Zjednoczonych , Stowarzyszenie Narodów Azji Południowo-Wschodniej , Światowa Organizacja Handlu .
Cechuje się on dużym pluralizmem religijnym , a główną grupą etniczną są Chińczycy .
Jest to jeden z największych portów i centrów finansowych świata .
Historia .
Singapur został wydzierżawiony w 1819 roku jako placówka handlowa od sułtanatu Johor przez Brytyjską Kompanię Wschodnioindyjską .
W 1826 roku Brytyjczycy kupili Singapur od sułtana .
Był odtąd wykorzystywany głównie jako baza morska .
Od 1867 stanowił część brytyjskich kolonii zwanych Straits Settlements .
Po I wojnie światowej Singapur stał się najważniejszą brytyjską bazą wojskową na Dalekim Wschodzie .
Zaprojektowany tak , aby mógł oprzeć się atakom morskim , w lutym 1942 roku został zaatakowany i 15 lutego zdobyty przez Japończyków od strony lądu , a następnie był przez nich okupowany do września 1945 roku .
W 1946 Singapur stał się oddzielną kolonią brytyjską , a pełną autonomię uzyskał w 1959 roku .
Na czele rządu stanął Lee Kuan Yew .
W 1962 roku rząd Singapuru wsparł rebelię w Brunei .
W 1963 Singapur przystąpił do Federacji Malezji , ale już w sierpniu 1965 roku został wydalony wskutek głosowania parlamentarnego stosunkiem głosów 126 za , 0 przeciw .
Niepodległa Republika Singapuru , należąca do brytyjskiej Wspólnoty Narodów , została utworzona 9 sierpnia 1965 roku .
W tym samym roku Singapur został członkiem ONZ .
