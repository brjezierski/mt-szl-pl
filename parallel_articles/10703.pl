Sinnai
Sinnai – miejscowość i gmina we Włoszech , w regionie Sardynia , w prowincji Cagliari .
Według danych na rok 2021 gminę zamieszkiwało 17 301 osób , 77,27 os./km² .
Graniczy z Burcei , Castiadas , Dolianova , Maracalagonis , Quartucciu , San Vito , Settimo San Pietro , Soleminis , Villasalto i Villasimius .
