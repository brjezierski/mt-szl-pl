Sinsheim
Sinsheim – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Rhein-Neckar , w powiecie Rhein-Neckar , siedziba wspólnoty administracyjnej Sinsheim .
Leży nad rzeką Elsenz , ok. 25 km na południowy wschód od Heidelbergu , przy autostradzie A6 , drogach krajowych B292 , B45 i linii kolejowej Bad Friedrichshall–Neckargemünd .
W Sinsheim znajduje się Muzeum Samochodów i Techniki `` ( Auto- und Technik Museum Sinsheim ) '' .
