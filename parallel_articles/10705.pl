Sint-Niklaas ( 1954 )
Sint-Niklaas ( M918 ) – belgijski trałowiec z lat 50 .
XX wieku , jedna z 26 zbudowanych dla Belgijskiej Marynarki Wojennej jednostek amerykańskiego typu `` Adjutant '' .
Stępkę okrętu położono w lipcu 1951 roku w stoczni Henry B. Nevins w City Island , a w skład marynarki wojennej Belgii został przyjęty w lutym 1954 roku .
W 1969 roku jednostkę zwrócono USA , po czym została przekazana Tajwanowi i wcielona do marynarki wojennej Republiki Chińskiej pod nazwą „ Yung Jen ” .
Okręt wycofano ze służby w 2012 roku .
Projekt i budowa .
Projekt trałowców typu `` Adjutant '' ( `` Blackbird '' ) powstał jako rozwinięcie drugowojennych jednostek typu `` YMS '' .
Okręty miały w większości zasilić floty państw sojuszniczych , w ramach Major Defense Acquisition Program ( MDAP ) .
