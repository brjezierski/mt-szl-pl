Aleksander Kwaśniewski
( ur .
15 listopada 1954 w Białogardzie ) – polski polityk i dziennikarz .
Prezydent Rzeczypospolitej Polskiej w latach 1995–2005 .
W latach 1985–1989 minister-członek Rady Ministrów w rządach Zbigniewa Messnera i Mieczysława Rakowskiego , w latach 1985–1990 członek prezydium Rady Ochrony Pamięci Walk i Męczeństwa , w latach 1988–1991 prezes Polskiego Komitetu Olimpijskiego .
Założyciel Socjaldemokracji Rzeczypospolitej Polskiej , w latach 1991–1995 poseł na Sejm I i II kadencji .
Życiorys .
Młodość i wykształcenie .
Syn Zdzisława Kwaśniewskiego ( 1921–1990 ) i Aleksandry z domu Bałasz ( 1929–1995 ) .
Jego ojciec mieszkał w Wilnie , po przesiedleniu z połowy lat 40. pracował początkowo w urzędzie repatriacyjnym .
Ukończył medycynę , był chirurgiem i kierownikiem przychodni kolejowej w Białogardzie , a także sędzią piłkarskim .
Matka Aleksandra Kwaśniewskiego wychowała się w Wilnie w rodzinie rosyjskojęzycznej i prawosławnej .
Jej rodzina po II wojnie światowej zamieszkała w Dojlidach .
Aleksandra Kwaśniewska była pielęgniarką , po zawarciu związku małżeńskiego zajmowała się rodziną i nie pracowała zawodowo .
Była siostrą białostockiego architekta sakralnego Michała Bałasza .
