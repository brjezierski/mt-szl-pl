Xibe
Xibe lub Sibo ( `` Sibe '' ; ) – grupa etniczna pochodzenia tunguskiego , zamieszkująca głównie północno-wschodnie Chiny i region Sinciang .
Jest jedną z 55 mniejszości narodowych oficjalnie uznawanych przez władze Chińskiej Republiki Ludowej .
Historia .
Xibe zamieszkali pierwotnie doliny rzek Nen i Sungari w środkowej Mandżurii .
Lud znany jest jako jeden z dziewięciu , które zostały pokonane przez Nurhaczego w bitwie pod Gure , w 1593 roku .
Znajdowali się pod luźnym panowaniem Mongołów Chorczin ( Chorczyn ) , nawet wtedy kiedy ci przeszli pod władanie mandżurskiej dynastii Qing .
Xibe zaczęli nawiązywać bezpośredni kontakt z Qingami i udzielali im wsparcia logistycznego w czasie kampanii wojskowych przeciwko Rosji .
W 1692 roku Mongołowie Chorczin przekazali swoją władzę nad ludami Xibe , Gūwalca oraz Dagurami cesarzowi Kangxi , w zamian za srebro .
