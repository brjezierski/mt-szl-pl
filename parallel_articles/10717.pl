Sinciang ( , wym .
[ ] ; pełna nazwa : Region Autonomiczny Sinciang-Ujgur , ujg .
شىنجاڭ ئۇيغۇر ئاپتونوم رايونى , Shinjang Uyghur Aptonom Rayoni ) – region autonomiczny w Chinach .
Jest to duży , rzadko zaludniony obszar , stanowiący ok. 1/6 terytorium kraju .
Sinciang sąsiaduje z Tybetem od południa , z prowincjami Qinghai i Gansu od południowego wschodu , z Mongolią od wschodu , Rosją od północy , a Kazachstanem , Kirgistanem , Tadżykistanem , Afganistanem , Pakistanem oraz kontrolowanymi przez Indie obszarami Kaszmiru od zachodu .
