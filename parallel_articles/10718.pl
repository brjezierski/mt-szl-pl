Sielec ( rejon iwacewicki )
Sielec ( , `` Sialec '' ; , `` Sielec '' ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie iwacewickim , w sielsowiecie Dobromyśl , nad Myszanką i w pobliżu jej ujścia do Szczary .
W dwudziestoleciu międzywojennym leżał w Polsce , w województwie nowogródzkim ; do 22 stycznia 1926 w powiecie słonimskim , następnie w powiecie baranowickim ; w gminie Dobromyśl .
Po II wojnie światowej w granicach Związku Radzieckiego .
Od 1991 w niepodległej Białorusi .
