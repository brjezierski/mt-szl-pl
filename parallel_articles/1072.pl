A. Malinowskiego , pracującego w Sokółce w guberni grodzieńskiej .
Rodzina ojca pochodziła z Wołogdy w północnej Rosji .
W 1892 roku Bogdanow ukończył gimnazjum w Tule uzyskując złoty medal i rozpoczął studia fizyczno-matematyczne na Uniwersytecie Moskiewskim oraz studia lekarskie na Uniwersytecie Charkowskim , gdzie związał się z Socjaldemokratyczną Partią Robotniczą Rosji .
Został aresztowany , osądzony za działalność rewolucyjną i zesłany do Kaługi .
Po trzech latach pobytu w Kałudze zesłano go do Wołogdy , gdzie odbywał praktyki lekarskie w miejscowym szpitalu psychiatrycznym .
W 1904 roku nie ukończywszy jeszcze odbywania kary zesłania , opuścił nielegalnie Rosję i wyjechał do Szwajcarii oraz Paryża , gdzie włączył się w działalność bolszewików przebywających na emigracji .
