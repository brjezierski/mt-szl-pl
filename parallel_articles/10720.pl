Skalica ( bułg .
`` Скалица '' ) – wieś w południowo-wschodniej Bułgarii , w obwodzie Jamboł , w gminie Tundża .
Według danych Narodowego Instytutu Statystycznego , 31 grudnia 2011 roku wieś liczyła 580 mieszkańców .
Przyroda .
W samym centrum wsi znajduje się miejsce Kajadżik bair , która usiane jest głazami .
