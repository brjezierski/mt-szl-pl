Skansen
Skansen ( `` muzeum skansenowskie '' , `` muzeum na wolnym powietrzu '' ( ang .
`` open-air museum '' ) ) – potoczne określenie muzeum na wolnym powietrzu , którego celem jest zaprezentowanie kultury ludowej danego regionu lub też ekspozycja obiektów zabytkowych ( archeologicznych , budowlanych , etnograficznych ) .
Polskie muzea skansenowskie zrzeszają się w Stowarzyszeniu Muzeów na Wolnym Powietrzu .
Najstarszym skansenem w Polsce jest Kaszubski Park Etnograficzny we Wdzydzach Kiszewskich .
Etymologia nazwy .
Nazwa tego typu muzeów wywodzi się od szwedzkiej nazwy własnej , parku `` Skansen '' , użytej dla otwartej w 1891 przez Artura Hazeliusa ekspozycji 150 przykładów architektury drewnianej z terenu całego kraju , na wyspie Djurgården na terenie Sztokholmu , gdzie po raz pierwszy przygotowano tego typu zbiór zabytków w Szwecji ( wcześniej powstał m.in .
skansen w Oslo ) .
Przeniesiono tam z różnych regionów kraju charakterystyczne dla nich budynki wiejskie .
