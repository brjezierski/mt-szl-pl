Skaratki
Skaratki ( `` Stara Wieś-Skaratki '' ; w potocznym użyciu stosowana również nazwa `` Stara Wieś '' ) – wieś w Polsce położona w województwie łódzkim , w powiecie łowickim , w gminie Domaniewice .
Wieś duchowna położona była w drugiej połowie XVI wieku w powiecie sochaczewskim ziemi sochaczewskiej województwa rawskiego .
Była wsią klucza chruślińskiego arcybiskupów gnieźnieńskich .
W latach 1975–1998 miejscowość administracyjnie należała do województwa skierniewickiego .
Odkryto tu pierwsze ślady człowieka w województwie łódzkim .
