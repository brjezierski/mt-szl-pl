Wola Skarbkowa
Wola Skarbkowa – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie radziejowskim , w gminie Osięciny .
Podział administracyjny .
Wieś duchowna , własność biskupstwa włocławskiego ( klucz włocławski ) , położona była w II połowie XVI wieku w powiecie brzeskokujawskim województwa brzeskokujawskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa włocławskiego .
Wieś sołecka – zobacz jednostki pomocnicze gminy w BIP .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 228 mieszkańców .
Jest dziesiątą co do wielkości miejscowością gminy Osięciny .
