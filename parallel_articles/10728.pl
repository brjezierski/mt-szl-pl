Skarżysko-Kamienna
Skarżysko-Kamienna ( do 24 kwietnia 1928 `` Kamienna '' ) – miasto w centralnej Polsce , północnej części województwa świętokrzyskiego , siedziba władz powiatu skarżyskiego .
Położona na Wyżynie Kieleckiej , u zbiegu Przedgórza Iłżeckiego , Garbu Gielniowskiego i Płaskowyżu Suchedniowskiego , nad rzeką Kamienną .
W województwie kieleckim lat 1950–1975 Skarżysko-Kamienna stanowiło – od roku 1954 , obok Kielc , Radomia , Ostrowca Świętokrzyskiego i Starachowic – powiat miejski .
Skarżysko-Kamienna tradycyjnie stanowi ważny ośrodek przemysłowy i węzeł komunikacyjny – drogowy i kolejowy .
Według danych z 31 grudnia 2017 r. miasto miało 45 953 mieszkańców .
W 2014 było drugim najbardziej zadłużonym miastem powiatowym w Polsce , a w 2017 było poza pierwszą dziesiątką tego rankingu .
Od 2015 w mieście funkcjonuje młodzieżowa rada miasta oraz budżet obywatelski .
Toponimia .
Do 24 kwietnia 1928 roku miasto nosiło nazwę `` Kamienna '' ; zwyczajowo i potocznie określane jest pierwszym członem nazwy – `` Skarżysko '' .
Położenie .
Miasto położone jest nad rzeką Kamienną oraz jej dopływami Kamionką , Bernatką i Oleśnicą , na pograniczu Płaskowyżu Suchedniowskiego i Garbu Gielniowskiego oraz na północnym obrzeżu Gór Świętokrzyskich .
