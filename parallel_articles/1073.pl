Na jego rozwój miały wpływ postacie dwóch przodków : jego stryja ( 1770–1830 ) i pradziadka , czarnoskórego Abisyńczyka Abrama Hannibala , który trafił do Rosji jako niewolnik , ale dzięki mądrości i odwadze stał się ulubieńcem cara Piotra I i otrzymał szlachectwo .
Trzecią osobą , która wywarła na niego wpływ , była niania Arina Rodionowna Jakowlewa , dzięki której poznał kulturę i problemy chłopów pańszczyźnianych .
Początkowo kształcili go francuscy guwernerzy .
Od 1811 przebywał w elitarnym Liceum w Carskim Siole ( obecnie Carskie Sioło przemianowano na Puszkin ) w pobliżu Petersburga .
Jego debiutanckie , pisane w liceum , utwory to dydaktyczno-satyryczne listy poetyckie , ody i elegie utrzymane w stylu klasycystycznym i preromantycznym .
