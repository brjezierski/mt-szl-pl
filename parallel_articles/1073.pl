Utwory te krążyły początkowo tylko w odpisach i zostały wydane dopiero w latach pięćdziesiątych XIX wieku .
Po opublikowaniu wierszy do ich autora entuzjastyczny list wysłał Czaadajew .
Antycarska działalność literacka spowodowała zsyłkę poety na południe Rosji .
Przed wyjazdem z Petersburga Puszkin zdążył jeszcze ogłosić w ( 1820 ) swój pierwszy poemat `` Rusłan i Ludmiła '' ( Руслан и Людмила , 1820 ) , który zwrócił powszechną uwagę .
Był to początek fascynacji bajronizmem , wprowadzonym przez Puszkina do literatury rosyjskiej .
Kolejne utwory powstające na zsyłce : `` Jeniec kaukaski '' ( 1822 ) , `` Fontanna Bachczyseraju '' ( Бахчисарайский фонтан , 1823 ) , `` Cyganie '' ( Цыганы , 1824 ) utrzymane są w tym stylu .
Na zesłaniu Puszkin przebywał najpierw w Kiszyniowie , potem w Odessie , na Kaukazie i na Krymie .
W tym czasie rozpoczął pracę nad poematem `` Eugeniusz Oniegin '' ( Евгений Онегин ) , pisząc w latach 1823–1824 trzy pierwsze rozdziały .
