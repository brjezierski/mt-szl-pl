Skawina
Skawina – miasto w województwie małopolskim , w powiecie krakowskim , siedziba gminy Skawina .
Miasto jest położone nad rzeką Skawinką , kilkanaście kilometrów na południowy zachód od centrum Krakowa .
Jest jednym z ośrodków miejskich aglomeracji krakowskiej .
Według danych GUS z 31 grudnia 2019 r. miasto liczyło 24 310 mieszkańców , natomiast obszar całej gminy zamieszkuje 43 586 osób .
Powierzchnia miasta wynosi 20,50 km² .
Było to miasto opactwa benedyktynów tynieckich w powiecie szczyrzyckim województwa krakowskiego w końcu XVI wieku .
W latach 1975–1998 Skawina należała administracyjnie do województwa krakowskiego .
