Skawinki
Skawinki – wieś w Polsce , położona w województwie małopolskim , w powiecie wadowickim , w gminie Lanckorona , na skraju Beskidu Makowskiego , u wschodnich podnóży góry Chełm ( 603 m n.p.m .
) , przy lokalnej drodze z Brodów do Palczy , w dolinie potoku Cedron ( dopływu Skawinki ) , który na terenie wsi ma swe źródliska .
Historia .
Pierwsze nieoficjalne wzmianki pochodzą z 1336 r. Lokowana przez Kazimierza Wielkiego 11 listopada 1359 .
Jako wieś królewska weszła wkrótce w skład starostwa niegrodowego lanckorońskiego i była z nim związana do 1889 r. , kiedy to znalazła się w dobrach izdebnickich , należących do Habsburgów .
Po I wojnie światowej stała się częścią Spółki Izdebnickie Zakłady Przemysłowe .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bielskiego .
