Skałat
Skałat ( ukr .
`` Скалат '' ) – miasto w rejonie podwołoczyskim na zachodniej Ukrainie w obwodzie tarnopolskim nad rzeką Gniłą dopływem Zbrucza , w paśmie Miodoborów , lokowane na prawie magdeburskim w roku 1600 ( bądź 1634 ) .
Historia .
Przed rokiem 1600 .
Pierwsza wzmianka o wsi Skałat leżącej w ziemi halickiej pochodzi z 1512 roku bądź z 1564
Lata 1600–1772 .
W latach 1600–1772 miasto w Rzeczypospolitej , powiecie trembowelskim ziemi halickiej województwa ruskiego .
