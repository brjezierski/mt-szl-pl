Skierniewice
Skierniewice – miasto na prawach powiatu , położone na Równinie Łowicko-Błońskiej , w połowie drogi między Warszawą a Łodzią , w województwie łódzkim .
Miasto było stolicą województwa skierniewickiego w latach 1975–1998 .
Miasto położone jest w dorzeczu Wisły , na Nizinie Środkowomazowieckiej , w południowej części Równiny Łowicko-Błońskiej .
Skierniewice były miastem duchownym arcybiskupstwa gnieźnieńskiego w ziemi rawskiej województwa rawskiego w 1792 roku .
Miasto należy do Związku Miast Polskich .
Według danych Głównego Urzędu Statystycznego z 31 grudnia 2021 roku w Skierniewicach mieszkały 45 933 osoby .
Nazwa miasta .
Istnieją dwie wersje pochodzenia nazwy miasta , jednak najbardziej prawdopodobne jest pochodzenie od nazwy „ skwierczącego ” kwiczoła – łownego ptaka z rodziny drozdów .
Położenie .
Skierniewice są otoczone od północy przez Puszczę Bolimowską , która stanowi fragment Bolimowskiego Parku Krajobrazowego .
Fragment zachodniej granicy miasta wyznacza Las Zwierzyniecki .
