18 kwietnia 1996 w Fort Lauderdale ) – amerykański raper i autor tekstów .
Początkowo zyskał rozgłos wraz z XXXTentacionem i kolektywem Members Only .
W 2017 roku wydał single `` Catch Me Outside '' i `` BabyWipe '' , oba znalazły się na jego mixtape ’ ie You Will Regret ( 2017 ) , który uzyskał status złota przez RIAA .
