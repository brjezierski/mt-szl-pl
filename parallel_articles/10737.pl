Skidniów
Skidniów – wieś w Polsce położona w województwie dolnośląskim , w powiecie głogowskim , w gminie Kotla .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa legnickiego .
Nazwa .
W 1295 w kronice łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księdze uposażeń biskupstwa wrocławskiego '' ) miejscowość wymieniona jest w zlatynizowanej formie `` Skidnewo '' .
