Skittles
Skittles – cukierki o smaku owocowym produkcji Wrigley Company od 2016 będący oddziałem Mars Incorporated .
Zawierają głównie cukier , syrop kukurydziany i olej palmowy .
Mają kształt soczewkowaty , występują w kilku kolorach .
Z jednej strony mają nadrukowaną , białą literę „ S ” .
Mylone bywają z cukierkami M & amp ; M 's , produkcji tej samej firmy .
