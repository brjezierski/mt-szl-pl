Analiza genetyczna ( badania pod kierownictwem Erika Tihelki ) wykazała , że pchły , uznawane dotąd za odrębny rząd , powinno zaliczać się do pasożytniczych wojsiłek ; pchły okazały się najbliżej spokrewnione z małą rodziną , której przedstawiciele żywią się nektarem .
Cykl rozwojowy pcheł .
Zależnie od temperatury cały cykl rozwojowy ( 1-4 ) trwa nie dłużej niż 2-3 tygodnie .
W niskich temperaturach cykl rozwojowy pcheł jest zahamowany .
