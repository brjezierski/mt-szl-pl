Pchły
Pchły ( Siphonaptera , Aphaniptera ) – rząd owadów zaliczanych do podgromady owadów uskrzydlonych , wtórnie bezskrzydłych .
Posiadają ciało bocznie spłaszczone , silnie rozwinięte odnóża , umożliwiające im długie skoki .
Narządy gębowe pchły są typu kłująco-ssącego , całe ciało pokrywa twardy oskórek ze szczecinkami lub ząbkami skierowanymi do tyłu .
Pchły osiągają wielkość od 1 do 6 mm , a nawet do 10 mm i są ciemno ubarwione .
Rozwój osobniczy pcheł odbywa się z przeobrażeniem zupełnym .
Są kosmopolitycznymi pasożytami zewnętrznymi ptaków i ssaków .
Systematyka .
