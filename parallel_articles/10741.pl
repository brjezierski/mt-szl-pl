Skokówka
Skokówka – wieś w Polsce położona w województwie lubelskim , w powiecie zamojskim , w gminie Zamość .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Miejscowość ta graniczy z Zamościem ( przy południowej granicy miasta ) .
Tutaj w 1542 r. w dworze obronnym w Skokówce , zwanym zameczkiem , urodził się przyszły kanclerz i hetman wielki koronny Jan Zamoyski .
W 1580 r. na gruntach Skokówki Jan Zamoyski założył Zamość .
To z tą miejscowością związany jest herb Gminy Zamość z wizerunkiem gryfa za kamiennym mostem symbolizującym jej bliski partnerski związek z miastem .
Pierwotna nazwa Zamościa brzmiała bowiem `` Zamoście '' .
Prawdziwy rozwój Skokówki nastąpił dopiero w okresie powojennym .
Dziś jest podmiejskim osiedlem przyciągającym nowych mieszkańców .
