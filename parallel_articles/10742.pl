Skoleks
Skoleks ( z łac. ``
scolex '' poprzez gr .
`` skōlēks '' – robak , czerw , inaczej : czerwioch ) , główka – przednia część ciała tasiemców właściwych ( Eucestoda ) uzbrojona w narządy czepne charakterystyczne dla poszczególnych rzędów – różnego rodzaju bruzdy ( botria i botrydia ) , przyssawki , kolce i haki , wspomagające przywarcie do ściany jelita żywiciela .
Służy do utrzymywania pasożyta w ciele żywiciela .
Obecność czterech przyssawek na skoleksie to charakterystyczna cecha tasiemców z rzędu Cyclophyllidea .
