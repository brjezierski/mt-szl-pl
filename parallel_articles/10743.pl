Skopin
Skopin ( ros .
`` Скопи́н '' ) – miasto w Rosji , w obwodzie riazańskim , 109 km na południe od Riazania .
Centrum administracyjne rejonu skopińskiego , w którego skład nie wchodzi , stanowiąc wydzielony okręg miejski obwodu riazańskiego .
W 2012 r. okręg miejski Skopin liczył 29 900 mieszkańców .
