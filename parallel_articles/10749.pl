Wola Skromowska
Wola Skromowska – wieś w Polsce położona w województwie lubelskim , w powiecie lubartowskim , w gminie Firlej .
Leży między dwoma rzekami Wieprzem i Tyśmienicą .
Do 1870 istniała gmina Wola Skromowska .
W latach 1954-1968 wieś należała i była siedzibą władz gromady Wola Skromowska , po jej zniesieniu w gromadzie Firlej .
W latach 1975–1998 miejscowość położona była w województwie lubelskim .
Wieś stanowi sołectwo gminy Firlej .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 499 mieszkańców .
We wsi funkcjonuje Ochotnicza Straż Pożarna oraz „ mleczarnia ” ( jedna z pierwszych w powiecie lubartowskim ) i spółdzielnia rolnicza z 1911 roku .
