Skrzydłowice
Skrzydłowice ( niem .
`` Skrzidlowitz '' ) – wieś w Polsce położona w województwie śląskim , w powiecie lublinieckim , w gminie Pawonków .
W latach 1975–1998 miejscowość administracyjnie należała do województwa częstochowskiego .
Nazwa .
Nazwa miejscowości wywodzi się od polskiej nazwy `` skrzydła '' z dodaną końcówką `` -wice '' .
Według niemieckiego językoznawcy Heinricha Adamy ’ ego pochodzi ona od pracujących na wiatr młynów zbożowych – wiatraków .
W swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu jako najstarszą nazwę miejscowości wymienia `` Skrzidlowitz '' podając jej znaczenie `` Windmuhlenplatz '' czyli w języku polskim `` Miejsce młynów wiatrowych '' .
Nazwa wsi została później fonetycznie zgermanizowana na `` Skrzidlowitz '' i utraciła swoje pierwotne znaczenie .
W 1936 roku hitlerowska administracja III Rzeszy , chcąc zatrzeć słowiańskie pochodzenie nazwy wsi , przemianowały ją ze zgermanizowanej na nową , całkowicie niemiecką nazwę `` Flügeldorf '' .
