Skrzypne
Skrzypne – wieś w Polsce położona w województwie małopolskim , w powiecie nowotarskim , w gminie Szaflary .
W latach 1975–1998 miejscowość administracyjnie należała do województwa nowosądeckiego .
Zabudowania i pola uprawne wsi znajdują się w dolinie potoku Pitoniowskiego na Kotlinie Nowotarskiej i na zboczach Pogórza Gubałowskiego , w historyczno-etnograficznym regionie Podhale .
Na terenie wsi działalność duszpasterską prowadzi Kościół rzymskokatolicki ( parafia MB Nieustającej Pomocy ) .
