Aleppo ( muhafaza )
Aleppo , Halab ( arab .
ممُحافظة حلب ) – jedna z 14 jednostek administracyjnych pierwszego rzędu ( muhafaza ) w Syrii .
Jest położona w północnej części kraju .
Graniczy od wschodu z muhafazą Rakka , od południa z muhafazą Hama , od zachodu z muhafazą Idlib a od północy z państwem tureckim .
W 2011 roku muhafazę zamieszkiwało 4 868 000 osób ; dla porównania , w 2004 było ich 4 045 166 , a w 1981 1 878 701 .
Wsie : Kabaszin , Kafr Dali Tahtani
