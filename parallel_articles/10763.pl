Liczba złożona
Liczba złożona – liczba naturalna większa od 1 niebędąca liczbą pierwszą , tj .
mająca co najmniej jeden naturalny dzielnik różny od 1 i niej samej .
Oznacza to , że liczbę złożoną można rozłożyć na iloczyn ( co najmniej ) dwóch liczb naturalnych większych od 1 i mniejszych od niej .
