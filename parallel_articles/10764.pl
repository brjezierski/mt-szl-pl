Skuter
Skuter ( ang .
`` scooter '' ) – rodzaj motoroweru lub motocykla , który prowadzi się bez obejmowania go nogami , z silnikiem i zbiornikiem paliwa najczęściej umieszczonymi pod kanapą .
Posiada stałe osłony nóg , obniżoną ramę ułatwiającą wsiadanie , a zamiast podnóżków podesty dające oparcie całej stopie .
Bardzo często pod kanapą można spotkać bagażnik .
W większości nowoczesnych konstrukcji stosowna jest bezstopniowa automatyczna skrzynia biegów pozwalająca na sprawne ruszanie i wygodną jazdę .
Najlżejsze , z silnikiem spalinowym 50cm³ , są zaliczane do kategorii motorowerów .
Małe koła i niska prędkość maksymalna sprawiają , że skutery są użyteczne w ruchu miejskim , ale rzadko sprawdzają się w terenie .
Obecnie wśród skuterów można wyróżnić segment maxi-skuterów .
Są one bardziej masywne , mają większe owiewki i napędzają je silniki o większej pojemności ( 125cm³ i więcej ) .
Posiadają także zazwyczaj więcej schowków .
Konstrukcja ich pozwala na wygodne pokonanie długiej trasy jednocześnie nie utrudniając przemieszczania się w mieście .
Największymi producentami skuterów są obecnie : Piaggio , Peugeot , Aprilia , Yamaha , Suzuki , Honda , PGO , TGB , Kymco , a importerami w Polsce : Romet , Junak Keeway , Motobi , Benzer , Zipp i Kingway .
Błędem jest utożsamianie skuterów z motorowerami .
Skuter to specyficzna konstrukcja jednośladów , może być zgodnie z prawem zarejestrowany zarówno jako motorower , jak i motocykl .
