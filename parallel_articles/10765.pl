Skyforger
Skyforger – łotewski zespół muzyczny , tworzący muzykę z gatunku folk metal , znany z pogańskiej tematyki tekstów swoich utworów .
Grupa powstała 1995 roku w Rydze .
Historia .
Wcześniej , w 1991 , główny przedstawiciel łotewskiego pagan/folk metalu rozpoczął karierę jako Grindmaster Dead , zespół grający brutal death/doom metal .
W latach 1992-1994 grupa wydała 3 nielegalne albumy , coraz bardziej fascynując się historią pogańskiej Łotwy .
Debiutanckim oficjalnym albumem była płyta demo `` Semigalls ` Warchant '' .
Zarówno muzyka , jak i teksty utworów Skyforgera ewoluowały , zawsze jednak obracając się w kręgu pogańskim .
Pierwsze dwie płyty były potężną dawką ostrego metalu , jednakże ze wstawkami ludowych instrumentów , a motywy muzyczne były przekształcone z ludowych tematów .
Tematem przewodnim ich tekstów były walki plemion zamieszkujących tereny obecnej Łotwy z Zakonem Krzyżackim .
Trzecia płyta , `` Latviešu strēlnieki '' , poświęcona była historii strzelców łotewskich w czasie I wojny światowej .
Nietypowa , bo czysto folkowa płyta to `` Zobena Dziesma '' , ogłoszona przez łotewską telewizję publiczną `` płytą roku '' w roku 2004 .
