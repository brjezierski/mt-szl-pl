Skype
Skype – komunikator internetowy firmy Microsoft , oparty na technologii przetwarzania danych w chmurze .
Skype umożliwia prowadzenie darmowych rozmów głosowych oraz obserwację rozmówcy poprzez kamerę internetową , a także płatnych rozmów z posiadaczami telefonów stacjonarnych lub komórkowych za pomocą technologii VoIP ( Voice over IP ) tzw .
usługa `` SkypeOut '' .
Oprócz tego Skype oferuje funkcje bezpośredniej wymiany informacji tekstowych za pomocą ręcznie wpisywanych wiadomości oraz przesył plików .
Historia .
Został stworzony przez Szweda Niklasa Zennströma i Duńczyka Janusa Friisa oraz grupę estońskich programistów z Tallinna .
Jego nazwa wzięła się ze zbitki sky-peer-to-peer , która przekształciła się w Skyper i ostatecznie w Skype .
Program działa na kilku platformach systemowych .
Skype w czerwcu 2005 zawarł umowę z polskim portalem internetowym Onet.pl .
Usługi tego komunikatora zostały włączone do oferty polskiego portalu .
We wrześniu 2005 amerykańska firma eBay ogłosiła porozumienie ze Skype Technologies i Niklasem Zennströmem dotyczące przejęcia tej spółki za 2,6 mld dolarów .
Do przejęcia doszło w październiku 2005 .
7 listopada 2007 wydano Skype 2.0 beta dla systemów Linux .
Jest to pierwsza wersja tego popularnego komunikatora obsługująca funkcję wideo na tej platformie .
