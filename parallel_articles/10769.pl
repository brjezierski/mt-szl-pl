Skórka ( grzyby )
Skórka ( ) – cienka zewnętrzna warstwa strzępek na kapeluszu i trzonie u grzybów owocnikowych .
Osłania miąższ grzyba i chroni go przed czynnikami zewnętrznymi , np .
skórka kapelusza mniej przepuszcza wodę , niż pozostałe struktury .
Jest mniej więcej odpowiednikiem epidermy u roślin , ale nie ma budowy tkankowej .
Zarówno cechy makroskopowe , jak i mikroskopowe skórki odgrywają dużą rolę przy identyfikacji wielu gatunków grzybów .
