Ales ( łac .
`` Alesium '' ) – miejscowość i gmina we Włoszech , w regionie Sardynia , w prowincji Oristano .
Siedziba diecezji Ales-Terralba , znajduje się tu katedra .
Graniczy z Albagiara , Curcuris , Gonnosnò , Marrubiu , Morgongiori , Palmas Arborea , Pau , Santa Giusta , Usellus , Villa Verde .
Według danych na rok 2004 gminę zamieszkiwało 1628 osób , 77,5 os./km2 .
W gminie znajduje się Zeppara , ok. 200 mieszkańców .
