Slaný ( stacja kolejowa )
Slaný – stacja kolejowa w miejscowości Slaný , w kraju środkowoczeskim , w Czechach .
Położona jest na linii Kralupy nad Vltavou - Louny .
Znajduje się na wysokości 280 m n.p.m .
Na stacji istnieje możliwość zakupu biletów na wszystkiego rodzaju pociągi w tym również międzynarodowe .
