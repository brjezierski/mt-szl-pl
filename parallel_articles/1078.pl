Alessandria ( stacja kolejowa )
Alessandria ( ang : `` Stazione di Alessandria '' ) – stacja kolejowa w Alessandrii , w regionie Piemont , we Włoszech .
Znajduje się tu 6 peronów .
Ruch .
Obsługa pasażerów odbywa się wyłącznie przez Trenitalia ( filia Ferrovie dello Stato ) w imieniu regionu Piemont .
Zatrzymują się tu pociągi regionalne , InterCity , Eurostar i Express .
W sumie około 332 pociągów obsługuje stację , którymi można dojechać m.in .
do : Torino Porta Nuova , Genova Piazza Principe i Novary .
