Sloten
Sloten - Jedno z najmniejszych miast w Holandii , najmniejsze we Fryzji , położone w gminie Gaasterland-Sloten .
Otrzymało prawa miejskie w 1426 roku .
Dziś bardzo popularne wśród turystów ze względu na dobre położenie jako przystań dla jachtów oraz ciekawe zabytki .
Część zabudowy mieszkalnej pochodzi z XVII wieku .
