Piestrzyca giętka
Piestrzyca giętka ( `` Helvella elastica '' Bull . )
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy opisał go Jean Baptiste Bulliard w 1785 r. i nadana przez niego nazwa jest ważna do dzisiaj .
Ma 27 synonimów .
Niektóre z nich :
Nazwa polska według M.A .
Chmiel .
Morfologia .
Twardo galaretowaty o średnicy 1,5–3 cm , kształtem przypominający siodło lub ucho .
