Smolewicze ( stacja kolejowa )
Smolewicze ( , ) – węzłowa stacja kolejowa w miejscowości Smolewicze , w rejonie smolewickim , w obwodzie mińskim , na Białorusi .
Leży na linii Moskwa - Mińsk - Brześć .
Rozpoczyna się tu linia do portu lotniczego Mińsk .
Składy podążające z Mińska w stronę lotniska , muszą w Smolewiczach zmienić kierunek jazdy , gdyż nie istnieje możliwość bezpośredniego wjazdu na linię z kierunku zachodniego .
Historia .
Stacja powstała w XIX w. na drodze żelaznej moskiewsko-brzeskiej pomiędzy stacjami Żodzino i Kołodziszcze .
Początkowo nosiła nazwę `` Wittgenstein '' .
