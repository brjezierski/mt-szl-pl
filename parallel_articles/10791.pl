Smokepurpp
Omar Jeffery Pineiro ( ur .
15 maja 1997 w Chicago ) znany pod pseudonimem jako Smokepurpp amerykański raper i producent muzyczny , który urodził się w Chicago , a obecnie mieszka w Miami .
Smokepurpp odniósł sukces dzięki platformie SoundCloud w 2015 .
Wypuścił wtedy piosenkę `` Ski Mask '' .
W krótkim czasie wystąpił jeszcze w piosence `` It 's nothin '' jako wykonawca gościnny i stworzył piosenkę `` Audi '' .
Pineiro wydał swój debiutancki komercyjny mixtape Deadstar 28 września 2017 r. a mixtape osiągnął 42 miejsce na liście albumów Billboard 200 .
