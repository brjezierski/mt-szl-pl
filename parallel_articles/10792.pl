Smokie
Smokie – brytyjski zespół muzyki pop i country rock pochodzący z Bradford , którego popularność w Europie przypadła na lata 70 .
XX wieku .
Historia .
Chris Norman , Alan Silson i Terry Uttley , będący filarami grupy , znali się ze szkoły .
Ich pierwszy zespół nazywał się „ The Yen ” , wkrótce przybrał nazwę „ The Sphynx ” .
Po ukończeniu szkoły zaczęli podróżować i grać koncerty w klubach i pubach robotniczych .
W 1968 po wielokrotnych zmianach nazwy grali jako „ The Elizabethans ” .
Mimo usilnych starań – rozsyłania nagrań demo , nagrania singli dla różnych wytwórni pod nazwą „ Kindness ” – nie udało im się zaistnieć .
W 1973 nastąpiła zamiana perkusisty na Pete ’ a Spencera .
W tym samym roku nowy manager zespołu Bill Hurley podjął działania mające na celu osiągnięcie sukcesu komercyjnego , nawiązując współpracę ze znanym duetem twórców przebojów glam rocka Chinn-Chapman .
W 1975 grupa przyjęła ostatecznie nazwę „ Smokie ”
W 1982 z zespołu odszedł wokalista Chris Norman i zaczął karierę solową , jednak ostateczne rozstanie z nim nastąpiło w 1986 .
Jego następcą został Alan Barton , zespół zwerbował także keyboardzistę Martina Bullarda .
Wkrótce odszedł także Pete Spencer , zastąpiony przez Steve ’ a Pinnella .
W 1995 roku w wypadku samochodowym zginął Alan Barton .
