Smolanka ( obwód tarnopolski )
Smolanka ( ukr .
Смоля́нка ) – wieś na Ukrainie w rejonie tarnopolskim należącym do obwodu tarnopolskiego .
Historia .
Właścicielem wsi był m.in .
hrabia Wiktor Ignacy Baworowski herbu Prus II , członek Stanów Galicyjskich , syn Józefa .
Ludzie .
Urodził się tu Leopold Dobiasz – pułkownik lekarz weterynarii doktor Wojska Polskiego .
