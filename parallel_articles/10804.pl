Snopków
Snopków – wieś w Polsce położona w województwie lubelskim , w powiecie lubelskim , w gminie Jastków .
Obok miejscowości przepływa rzeka Ciemięga , lewy dopływ Bystrzycy .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa lubelskiego .
Wieś stanowi sołectwo gminy Jastków .
Według Narodowego Spisu Powszechnego z roku 2011 wieś liczyła 928 mieszkańców .
