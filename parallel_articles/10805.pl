Snorri Sturluson
Snorri Sturluson ( ur .
1179 , zm .
23 września 1241 ) – islandzki poeta , historyk , polityk i przywódca .
Życiorys .
Autor `` Eddy prozaicznej '' i historii nordyckich władców `` Heimskringla '' .
Jest również uważany za prawdopodobnego autora `` Sagi o Egilu '' .
W 1206 Snorri osiadł w Reykholt .
Dał się poznać nie tylko jako poeta , ale również jako polityk i wpływowy przywódca islandzki .
Dwukrotnie pełnił funkcję `` lögsögumaður '' ( przewodniczącego ) w islandzkim parlamencie ( Althing ) w latach 1215–1218 i 1222–1231 .
Latem 1218 Snorri pożeglował z Islandii do Norwegii .
Poparł plany króla norweskiego Haakona IV dotyczące podporządkowania Islandii władzy Norwegii , jednak w późniejszym czasie został uwikłany w nieudaną rebelię przeciw królowi Haakonowi IV i z jego rozkazu został zamordowany 23 września 1241 w swoim domu w Reykholt .
Jako historyk Snorri wysunął w `` Eddzie prozaicznej '' teorię , że mityczni bogowie byli początkowo zmarłymi wodzami i królami plemiennymi , których kult przeistaczał się później po ich śmierci w kult postaci boskich .
