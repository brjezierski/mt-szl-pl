Snæfellsnes
Snæfellsnes – półwysep w zachodniej Islandii , położony na zachód od miasta Borgarfjörður .
Krajobraz półwyspu Snaefellsnes nazywane jest niekiedy „ Islandią w miniaturze ” , ponieważ znaleźć tu można wiele typowych dla tego kraju krajobrazów .
Centralnym punktem regionu jest wulkan Snæfellsjökull , który może być uznany nieomalże za symbol Islandii .
Jego wysokość sięga 1446 m n.p.m. , a na jego szczycie znajduje się lodowiec .
Przy dobrej widoczności wulkan widoczny jest ze stolicy kraju , Reykjavíku , położonej około 120 km stąd .
Wulkan znany jest dzięki francuskiemu pisarzowi Juliuszowi Verne ’ owi , który umieścił w nim miejsce akcji swojej książki `` Podróż do wnętrza Ziemi '' .
Wokół wulkanu Snæfellsjökull rząd islandzki utworzył park narodowy Snæfellsjökull .
Na północnym wybrzeżu półwyspu znajduje się kilka miasteczek i portów rybackich , z których ważniejszymi są : Ólafsvík , Grundarfjörður , Stykkishólmur oraz Búðardalur .
Główną drogą na półwyspie jest droga nr 54 , która biegnie wzdłuż jego południowego i północnego wybrzeża .
