Soacha
Soacha – miasto w środkowej Kolumbii , w Kordylierze Wschodniej ( Andy Północne ) .
Położone jest w zespole miejskim Bogoty , na jego południowo-zachodnich obrzeżach .
Położone jest na wysokości około 2565 metrów .
Według spisu ludności z 30 czerwca 2018 roku miasto liczyło 655 025 mieszkańców .
W Soacha funkcjonuje wydobycie węgla kamiennego .
Dobrze rozwinięty jest przemysł cementowy , gumowy , maszynowy i włókienniczy .
Miasto jest ośrodkiem handlowym regionu upraw m.in .
kukurydzy , kawowca i warzyw .
W mieście znajduje się siedziba rzymskokatolickiej diecezji Soacha .
18 sierpnia 1989 roku w Soacha dokonano zamachu na kandydata na prezydenta Luisa Galána , który podczas swej kampanii prezydenckiej odwiedzał to miasto .
