Majdan Sobieszczański
Majdan Sobieszczański – wieś w Polsce położona w województwie lubelskim , w powiecie lubelskim , w gminie Niedrzwica Duża .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa lubelskiego .
Wieś stanowi sołectwo gminy Niedrzwica Duża .
Według Narodowego Spisu Powszechnego z roku 2011 wieś liczyła 176 mieszkańców .
Historia .
Majdan Sobieszczański folwark w powiecie lubelskim , gminie i parafii Niedrzwica .
Rozległy na mórg 110 w tym : grunta orne i ogrody mórg 77 , łąk mórg 6 , lasu mórg 27 , budynków z drzewa posiadał 8 .
Folwark ten w roku 1873 oddzielony został od dóbr Sobieszczany z nomenklatur C .
