Sobącz
Sobącz ( kaszb .
`` Sobóńcz '' ) – wieś w Polsce , położona w województwie pomorskim , w powiecie kościerskim , w gminie Liniewo , nad jeziorem Sobackim .
Wieś jest siedzibą sołectwa Sobącz , w którego skład wchodzi również osada Bukowe Pole .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
