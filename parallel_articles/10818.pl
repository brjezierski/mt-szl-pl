Sochaczew
Sochaczew – miasto w centralnej Polsce , w województwie mazowieckim w powiecie sochaczewskim , nad rzekami Bzurą , Utratą i Pisią .
Do 1954 r. siedziba wiejskich gmin Chodaków i Kozłów Biskupi .
W latach 1975–1998 miasto administracyjnie należało do województwa skierniewickiego .
Jest stolicą powiatu sochaczewskiego oraz siedzibą władz gminy wiejskiej Sochaczew .
Jest jednym z najstarszych miast mazowieckich .
