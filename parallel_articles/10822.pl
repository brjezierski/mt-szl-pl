Sodoma i Gomora
Sodoma i Gomora ( hebr .
`` מהפכת סדום ועמורה '' ) – motyw biblijny pochodzący ze starotestamentowej Księgi Rodzaju , dotyczący zniszczenia przez Boga położonych w dolinie Siddim miast kananejskich Sodomy i Gomory na skutek niegodziwości ich mieszkańców .
Przekaz biblijny .
Według Biblii Abraham po powrocie od króla Egiptu rozdzielił ziemię kananejską pomiędzy siebie i Lota , który wybrał obszar w pobliżu Sodomy , otrzymawszy prawo pierwszeństwa wyboru .
Miasto wkrótce zostało pokonane przez Asyryjczyków , którzy nałożyli na jego mieszkańców daninę .
Po dwunastu latach Sodoma zbuntowała się przeciwko Asyrii .
Miasto wsparł Abraham , za co Bóg Jahwe obiecał mu wielką nagrodę .
Gdy Sodoma urosła w siłę , niechętnie przyjmowała przybyszów .
Według Biblii mieszkańcy Sodomy i Gomory byli niegodziwi , rozpustni , pogardzali prawem boskim i ludzkim .
Bóg Jahwe zamierzał je zniszczyć , ale za namową Abrahama zgodził się , że daruje życie jego mieszkańcom , jeśli wśród nich znajdzie się przynajmniej dziesięciu sprawiedliwych .
W tym celu wysłał do Sodomy dwóch swoich aniołów , którzy spędzili noc w domu Lota .
Przed nocą mężczyźni z miasta otoczyli dom Lota , by współżyć z jego gośćmi .
Lot zaoferował im w zamian swoje dwie córki-dziewice , jednak oni odmówili .
Wtedy zaryglował drzwi domu .
Mieszkańcy miasta chcieli je wyważyć , więc aniołowie porazili napastników ślepotą .
Lotowi nakazali , by wyprowadził z Sodomy najbliższych , bo rankiem miasto zostanie zniszczone .
Przyszli zięciowie Lota odmówili pójścia z nim .
Z nastaniem świtu aniołowie wyprowadzili Lota , jego żonę i dwie córki za miasto i nakazali uciekać w góry bez oglądania się za siebie .
Żona Lota nie wytrzymała tej próby i wbrew woli Boga obejrzała się .
Natychmiast też zamieniła się w słup soli .
Lot za pozwoleniem Boga udał się do pobliskiego Soar , który dzięki temu został ocalony .
Jahwe zniszczył miasta wraz z mieszkańcami i roślinnością w całym okręgu : Sodomę i Gomorę , Seboim i Admę ( Pwt 29:22 ) .
Wczesnym rankiem Abraham ujrzał , jak z pobliskich miast unosi się gęsty dym .
Wkrótce Lot opuścił Soar i zamieszkał z córkami w jaskini .
Badania archeologiczne .
Archeolodzy umiejscawiają Sodomę i Gomorę na dnie Morza Martwego bądź utożsamiają z ruinami miast występującymi na jego wschodnim ( jordańskim ) wybrzeżu w rejonie Siddim .
Najbardziej prawdopodobnymi miejscami położenia Sodomy i Gomory są odpowiednio stanowiska archeologiczne : Bab edh-Dhra oraz Numeira .
Możliwe , że obie osady zostały zniszczone przez katastrofy naturalne , których gwałtowność dała początek opowieści biblijnej .
W Izraelu w pobliżu południowej części wybrzeża Morza Martwego znajduje się wzgórze Sodom , które wyróżnia się budową geologiczną – prawie w całości zbudowane jest z halitu ( soli kamiennej ) .
Z powodu erozji nastąpiło rozdzielenie masywu .
Jeden z oddzielonych filarów nazwany jest potocznie „ żona Lota ” .
Związek frazeologiczny .
Zwrot `` sodoma i gomora '' jest także związkiem frazeologicznym , który oznacza wielkie zamieszanie , awantura czy bałagan , gniazdo rozpusty , siedlisko zła moralnego i zepsucia obyczajów i miejsce przeklęte ; chaos i bałagan .
