Soja warzywna
Soja warzywna , s. owłosiona , s. zwyczajna , s. szorstka ( `` Glycine max '' ( L. ) Merr . )
– gatunek rośliny jednorocznej należący do rodziny bobowatych .
Pochodzi z południowo-wschodniej Azji .
W Polsce jest rzadko uprawiana , czasami przejściowo dziczejąca .
Wpływ na zdrowie .
Metaanaliza przeprowadzona na badaniach obejmujących 452 916 uczestników wykazała liniową zależność pomiędzy spożyciem soi i obniżeniem ryzyka wystąpienia raka piersi .
Mąka sojowa zawiera inhibitor trypsyny , który może być przyczyną przerostu trzustki .
Hipertrofię i hiperplazję trzustki obserwowano u zwierząt po podaniu surowej mąki sojowej lub oczyszczonego inhibitora trypsyny .
W postaci surowej soja zawiera wiele składników mogących negatywnie wpłynąć na organizm odżywiających się nią zwierząt .
Czynniki antyodżywcze zmniejszają ilość przyswajanych aminokwasów , witamin i soli mineralnych .
Do właściwego wykorzystania substancji odżywczych zawartych w soi konieczna jest obróbka cieplna .
Zaobserwowano spadek ilości wytwarzanego testosteronu u mężczyzn spożywających produkty sojowe .
Zawarte w nasionach soi fitoestrogeny mogą potencjalnie wpływać na zdrowie zarówno kobiet , jak i mężczyzn .
Badania dostarczyły słabych przesłanek do stwierdzenia pozytywnych skutków spożycia soi na zdrowie kobiet .
Między innymi badano zmniejszenie ryzyka wystąpienia nowotworów w okresie poprzedzającym menopauzę .
Badania wpływu produktów sojowych na zdrowie mężczyzn dotyczyły między innymi wpływu na płodność i jakość nasienia .
Negatywny wpływ na jakość nasienia nie został potwierdzony .
