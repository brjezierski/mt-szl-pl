Alfabet
Alfabet , abecadło ( od stgr .
nazw pierwszych liter alfabetu : `` alfa '' i `` beta '' lub z ) – najpopularniejszy system zapisywania mowy .
Termin ten bywa używany w kilku znaczeniach , co jest źródłem licznych nieporozumień w dziedzinie historii i teorii pisma .
Nieściśle „ alfabetami ” nazywa się też systemy pisma , które nimi nie są ( pseudoalfabety ) .
Nazwa „ alfabet ” i nazwy pokrewne .
Greckie pismo zostało nazywane `` alfabetem '' ( skąd nazwa ta rozprzestrzeniła się też na pismo łacińskie ) , cyrylickie – `` azbuką '' , polskie `` abecadłem '' , ze względu na zestaw znaków ( sens 2 . )
i porządek alfabetyczny ( sens 3 . )
danego pisma , z wykorzystaniem nazw pierwszych liter ( polskie : „ a , be , ce , de ” ; greckie : „ alpha , beta ” ; starocyrylickie : „ az , buki ” ) .
Systemy pisma pokrewne alfabetom ( sens 1 . )
zostały w XX wieku nazwane z wykorzystaniem tego samego mechanizmu .
Pismo spółgłoskowe nazywa się abdżadem , ze względu na nazwy pierwszych czterech liter w piśmie arabskim ( „ ' a , b , dż , d ” ) , będącym przedstawicielem tego typu pisma ; pismo alfabetyczno-sylabiczne nazywa się abugidą ze względu na nazwy czterech pierwszych liter w piśmie etiopskim ( „ a , bu , gi , da ” ) , będącym przedstawicielem tego typu pisma .
