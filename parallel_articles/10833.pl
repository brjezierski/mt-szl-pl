Sokrates ( imię )
Sokrates – imię męskie pochodzenia greckiego , ( `` Sōkrátēs '' ) , które powstało z członów ( `` sō̃s '' ) „ cały , zdrowy , bezpieczny ” i ( `` krátos '' ) „ siła , moc ” .
Imię to rozsławił filozof Sokrates .
W Kościele katolickim są święci o tym imieniu .
Sokrates imieniny obchodzi 19 kwietnia , 17 września i 20 września .
