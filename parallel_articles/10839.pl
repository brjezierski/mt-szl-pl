Soledad ( peruwiańska telenowela )
Soledad ( `` Soledad '' ) – peruwiańska telenowela z 2000 roku produkcji America Producciones licząca 200 odcinków .
w rolach głównych Coraima Torres i Guillermo Pérez .
Fabuła .
Alma Díaz del Castillo od śmierci męża poświęciła się czwórce swych dzieci : Soledad , Antonii , Fátimie i Juanjowi .
Najstarsza Soledad jest skromną sekretarką , ma narzeczonego , który mieszka niedaleko , także w niebogatej dzielnicy , i w którym właściwie nie jest zakochana .
Przed ślubem swej siostry Antonii Soledad idzie do kwiaciarni zamówić kwiaty na uroczystość .
Tu poznaje Migueal Angela Olivaresa , w którym zakochuje się od pierwszego wejrzenia .
Miguel Angel jest szefem Beauty Corporation , dużej firmy kosmetycznej .
Soledad robi na nim spore wrażenie – Miguel Angel , choć żonaty , nie przestał być czuły na urodę innych kobiet : miewa przygody zwłaszcza z modelkami pracującymi dla jego firmy .
Żoną Miguela Angela jest Victoria Alvarez .
To kobieta dojrzała , dużo starsza od swego męża , bardzo o niego zazdrosna .
Ma obsesję na punkcie swojej twarzy , ciała i wieku – chciałaby , by żaden z tych elementów nie zmienił się przez ostatnie 20 lat .
Zrządzeniem losu , Soledad zaczyna pracować w Beauty Corporation jako sekretarka Miguela Angela .
Bliskość szefa sprawi , że Soledad zapomni o wszelkich zasadach , które wpajała jej matka i odda się namiętności .
