Alfons Zgrzebniok
Alfons Alfred Zgrzebniok pseudonim „ Rakoczy ” ( ur .
16 sierpnia 1891 w Dziergowicach , zm .
31 stycznia 1937 w Marcinkowicach ) – polski nauczyciel , działacz samorządowy , śląski działacz polityczny , komendant powstań śląskich , według długoletniej tradycji historiograficznej jeden z założycieli Związku Powstańców Śląskich , choć faktycznie nie brał udział w tym procesie , `` prawa ręka '' wojewody pomorskiego Stefana Kirtiklisa , wicewojewoda białostocki .
Życiorys .
Urodził się w Dziergowicach w patriotycznej wielodzietnej rodzinie polskiej Juliusza Zgrzebnioka , właściciela drobnego gospodarstwa rolnego , prowadzącego jednocześnie warsztat szewski i Eleonory z Rakoczych , prowadzącej mały sklep spożywczy .
Ojciec był współzałożycielem Towarzystwa Rolników i Robotników .
Alfons Alfred był najstarszym z dziewięciorga rodzeństwa .
Miał trzech braci : Antoniego , Franciszka i Jana , oraz pięć sióstr : Marię , Ludwinę ( właściwie Albertynę ) , Reginę , Agnieszkę i Eufrozynę .
Ukończył szkołę podstawową w Dziergowicach , jednak ze względu na słabą znajomość języka niemieckiego przyjęto go do gimnazjum dopiero po rocznych korepetycjach w Wodzisławiu .
Ukończył gimnazjum w Raciborzu w 1909 .
Podczas nauki ze względu na przynależność do Kółka Narodowego groziło mu relegowanie z uczelni .
Wydalenia ze szkoły uniknął dzięki wysokim wynikom w nauce .
Następnie w latach 1909–1914 studiował filozofię , ekonomię i teologię na Uniwersytecie Wrocławskim , gdzie wstąpił do wrocławskiego koła polskiej tajnej organizacji „ Zet ” .
Wybuch I wojny światowej spowodował , że Zgrzebniok musiał przerwać naukę .
W czasie I wojny światowej w listopadzie 1914 został powołany do armii niemieckiej , walczył na froncie zachodnim i wschodnim .
W kampanii został trzykrotnie ranny , zdobył stopień porucznika .
Kilka dni przed końcem wojny został zdemobilizowany .
Po zakończeniu wojny stacjonował w garnizonie kozielskim .
Pod koniec 1918 roku z polecenia Józefa Dreyzy , komendanta Polskiej Organizacji Wojskowej Górnego Śląska , organizował struktury POW GŚl .
w powiecie kozielskim .
Mianowany komendantem POW GŚl .
na powiat kozielski oraz raciborski .
Zorganizował akcję zdobycia broni oraz amunicji z kozielskich koszar , w której sam wziął udział mając na sobie niemiecki mundur .
Wprowadził niemieckich żołnierzy w błąd fałszywymi rozkazami sugerującymi , że Zgrzebniok dowodzi tajną operacją niemieckiego dowództwa .
Sposób ten wykorzystywał wielokrotnie w zdobywaniu broni dla swoich ludzi .
Raz udało mu się nawet dzięki niemu uniknąć wykrycia magazynów broni podczas przeszukiwań siedziby „ Strzechy ” w Raciborzu przez niemiecką policję .
Występując w roli niemieckiego oficera zaofiarował swoją pomoc zwodząc Grenzschutz dzięki czemu udało się wynieść broń w bezpieczniejsze miejsce .
Aresztowany przez Grenzschutz , uciekł z więzienia .
Dzięki swojej działalności stał się „ największym polskim bandytą w okręgu kozielskim ” , a jego podobizna trafiła na listy gończe .
Ukrywał się w Polsce .
Dnia 17 kwietnia wraz z Józefem Grzegorzkiem , Wiktorem Rumpfeldem i Adamem Całką wziął udział w czteroosobowej delegacji do Poznania mającej na celu powiadomienie generała Józefa Dowbor-Muśnickiego o decyzji wybuchu powstania na Śląsku .
Był jednym z autorów planu powstania , przedstawionego Naczelnej Radzie Ludowej w Poznaniu 19 kwietnia 1919 .
Alfons Zgrzebniok wstąpił do założonego w 1902 przez księdza Pawła Brandysa Towarzystwa Rolników i Robotników , którego członkami byli m.in .
Juliusz Zgrzebniok , Jan Niemiec i Juliusz Kubica .
Od stycznia 1919 był jednym z najważniejszych członków Polskiej Organizacji Wojskowej Górnego Śląska ( POWGŚl ) .
