Szczawno-Zdrój
Szczawno-Zdrój ( ) – miasto uzdrowiskowe – gmina miejska w województwie dolnośląskim , w powiecie wałbrzyskim , położone jest w południowo-zachodniej Polsce na Dolnym Śląsku w Sudetach środkowych
W latach 1975–1998 miasto administracyjnie należało do województwa wałbrzyskiego .
Według danych GUS z 31 grudnia 2019 r. miasto miało 5569 mieszkańców .
Położenie .
Według danych z 1 stycznia 2010 r. powierzchnia miasta wynosiła 14,74 km² .
Miasto stanowi 2,89 % powierzchni powiatu .
Miasto jest położone w Sudetach środkowych , na pograniczu Gór Wałbrzyskich i Pogórza Wałbrzyskiego w bezpośrednim kontakcie z Wałbrzychem .
Leży na wysokości ok. 410 m n.p.m .
u podnóża góry Chełmiec ( 850 m n.p.m . )
najwyższego szczytu masywu Chełmca , drugiego szczytu Gór Wałbrzyskich , wznoszącego się na południe od miasta , oraz u podnóża Góry Parkowej , zwanej zwyczajowo Wzgórzem Gedymina , będącej częścią Wzniesień Ptasiej Kopy–Stróżka , rozprzestrzeniających się na wchód od miasta .
Według danych z 2002 Szczawno-Zdrój ma obszar 14,87 km² , w tym : użytki rolne ( grunty orne i trwałe użytki zielone ) 36 % , nieużytki i obszary leśne 64 % .
Szczawno-Zdrój graniczy z dwoma miastami : Wałbrzych i Boguszów-Gorce oraz z gminą Stare Bogaczowice .
Toponimia .
Do stycznia 1935 obowiązywała nazwa `` Ober Salzbrunn '' .
Na fali przydawania uzdrowiskom ze względów promocyjnych predykatu `` Bad '' , 9 stycznia 1935 r. zmieniono tę nazwę na `` Bad Salzbrunn '' .
W 1946 roku wprowadzono urzędowo nazwę `` Szczawno Zdrój '' , zastępując nazwę niemiecką , choć przez krótki czas po wojnie funkcjonowały także nazwy `` Słońsk '' i `` Solice Zdrój '' .
Nazwa miejscowości Szczawno-Zdrój pisana z łącznikiem ( dywizem ) jest formą zatwierdzoną urzędowo .
