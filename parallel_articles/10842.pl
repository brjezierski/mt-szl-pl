Solihull
Solihull – miasto w hrabstwie West Midlands , w Anglii , w dystrykcie metropolitalnym Solihull .
Jest częścią West Midlands conurbation .
Jest położone nad rzeką Blythe , 14,5 km na południe od centrum Birmingham .
W 2001 roku miasto liczyło 94 753 mieszkańców .
W mieście rozwinął się przemysł samochodowy .
