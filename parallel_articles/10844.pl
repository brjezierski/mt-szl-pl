Solingen
Solingen – miasto na prawach powiatu w zachodniej części Niemiec , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf , na południe od Zagłębia Ruhry .
Leży na obrzeżu górzystego regionu Bergisches Land , w którym jest drugim co do wielkości miastem ( za graniczącym z nim Wuppertalem ) .
Po reformie terytorialnej w 1929 w wyniku włączenia okolicznych gmin w granice miasta , liczba mieszkańców przekroczyła 100 000 , zyskując tym samym status dużego miasta ( `` Großstadt '' ) .
Miasto jest znane z produkcji noży .
Unikatowa jest też sieć trolejbusowa , która funkcjonuje jeszcze tylko w dwóch innych niemieckich miastach .
