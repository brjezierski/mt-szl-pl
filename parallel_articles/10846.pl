Sollefteå
Sollefteå – szwedzkie miasto w regionie Västernorrland , na terenie historycznej prowincji Ångermanland .
Według danych z 31 grudnia 2005 miasto zamieszkiwane było przez 8530 osób .
Powierzchnia miasta wynosi 9,41 km² .
W mieście znajduje się kompleks skoczni narciarskich Hallstabacken zbudowany w 1934 .
Odbyły się wówczas Mistrzostwa Świata w Narciarstwie Klasycznym 1934 .
W 2003 roku odbyły się tu Mistrzostwa Świata Juniorów w Narciarstwie Klasycznym .
Z Sollefteå pochodzi Frida Karlsson , szwedzka biegaczka narciarska , multimedalistka mistrzostw świata .
