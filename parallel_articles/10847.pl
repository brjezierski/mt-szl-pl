Gmina Solna
Gmina Solna ( szw .
`` Solna kommun '' lub `` Solna stad '' ) – gmina w Szwecji , w regionie Sztokholm posiadająca status miasta .
Leży bezpośrednio na północ od ścisłego centrum Sztokholmu .
W całości należy do `` tätortu '' Sztokholm i pomimo odrębnego statusu prawnego stanowi integralną część tegoż zespołu miejskiego .
Pod względem zaludnienia Solna jest 33. gminą w Szwecji .
Zamieszkuje ją 67 957 osób , z czego 50,68 % to kobiety ( 34 441 ) i 49,32 % to mężczyźni ( 33 516 ) .
Na każdy kilometr kwadratowy przypada 3576,68 mieszkańca ( stan na 1 listopada 2010 r. ) .
Pod względem wielkości gmina zajmuje 288. miejsce .
Wschodnia część gminy należy do `` nationalstadsparken '' , zespołu parków i obszarów ważnych z kulturalnego i historycznego punktu widzenia ( m.in .
pałac Ulriksdal czy Hagaparken ) .
Gmina jest dobrze skomunikowana , przez jej teren przebiegają obie odnogi niebieskiej linii sztokholmskiego metra , dwie ważne linie kolejowe ( Ostkustbanan i Mälarbanan ) obsługiwane przez podmiejski `` pendeltåg '' oraz linia szybkiego tramwaju łącząca gminę ze wschodnią i południową częścią aglomeracji ( `` tvärbanan '' ) .
Ponadto w niedalekiej przyszłości w okolice stacji kolejowej Solna dotrze żółta linia metra łącząca gminę z centrum Sztokholmu i południowymi przedmieściami miasta .
Całością transportu publicznego na terenie gminy zarządza Storstockholms Lokaltrafik AB ( w skrócie SL ) .
