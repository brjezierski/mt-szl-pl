Alfonsa Muttathupadathu
Alfonsa Muttathupadam od Niepokalanego Poczęcia , mal .
: അല്‍ഫോണ്‍സാ മുട്ടത്തുപാടം , `` Alphonsa dell ’ Immacolata Concezione '' ( ur .
19 sierpnia 1910 w Kudamaloor w Kerali , zm .
28 lipca 1946 w Bharananganam w Kerali ) – klaryska ( OCPA ) , święta , pierwsza Hinduska kanonizowana przez Kościół katolicki .
Była członkinią Syromalabarskiego Kościoła katolickiego , który uznaje papieża i pozostaje w unii z Watykanem .
`` Alphonsamma '' , jak nazywano ją w Indiach , pochodziła z biednej rodziny i od dzieciństwa doświadczała cierpienia .
Jej matka zmarła , gdy Alfonsa miała 3 miesiące , a jej wychowaniem zajmowała się ciocia , członkini Syromalabarskiego Kościoła katolickiego .
W wieku 17 lat Alfonsa wstąpiła do zakonu klarysek od wieczystej Adoracji .
Przez krótki okres pracowała jako nauczycielka w szkole podstawowej , jednakże musiała porzucić tę pracę z powodu choroby .
Pierwsze śluby złożyła w 1931 , a śluby zakonne w 1936 roku .
Od 1936 na trzy lata jej dolegliwości ustąpiły , co Kościół przypisuje wstawiennictwu św .
Teresy z Lisieux .
Następnie jej dolegliwości powróciły i jeszcze się wzmogły .
Swoje cierpienia Alfonsa ofiarowała Bogu w intencji nawrócenia grzeszników , stosując motto `` płonąć jak ogień , aby oświecić innych '' .
Wstawiennictwu Afonsy przypisywana jest seria cudownych uzdrowień , do których dochodziło wśród modlących się u jej grobu .
W corocznych pielgrzymkach na grób św .
Alfonsy biorą udział katolicy , muzułmanie , a także Hindusi .
Diecezjalny proces beatyfikacji rozpoczął biskup Palai w 1955 roku .
Aktu ogłoszenia Alfonsy błogosławioną dokonał św .
Jan Paweł II 8 lutego 1986 , a kanonizacji Benedykt XVI 12 października 2008 .
Jej wspomnienie liturgiczne w Kościele katolickim obchodzone jest w dzienną pamiątkę śmierci .
