Sonneberg
Sonneberg – miasto powiatowe w Niemczech , w kraju związkowym Turyngia , siedziba powiatu Sonneberg .
Leży u podnóża Lasu Turyńskiego , liczy 23 796 mieszkańców ( 2013 ) .
Począwszy od XVIII wieku miasto znane głównie jako ośrodek produkcji zabawek .
Siedziba przedsiębiorstwa Piko , specjalizującego się w modelarstwie kolejowym .
W mieście znajduje się stacja kolejowa z dworcem Sonneberg Hauptbahnhof .
31 grudnia 2013 do miasta przyłączono gminę Oberland am Rennsteig , która stała się automatycznie jego dzielnicą .
