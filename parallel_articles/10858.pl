Sony
– japoński producent elektroniki użytkowej i zarazem jeden z największych światowych producentów w tej branży , założony 7 maja 1946 roku przez Masaru Ibukę i Akio Moritę w Tokio jako .
Sony jest notowane na giełdach tokijskiej ( TSE ) i nowojorskiej ( NYSE ) .
Firma Sony jest znana z wprowadzonego na rynek w 1979 r. przenośnego odtwarzacza kasetowego „ Walkman ” .
Wprowadziła też i wypromowała takie znaki towarowe jak Discman czy Trinitron .
Wśród jej produktów znajduje się MiniDisc , seria konsoli wideo PlayStation , pamięci Memory Stick czy seria komputerów VAIO .
