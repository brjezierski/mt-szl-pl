Sopron
Sopron ( , ) – miasto i region na Węgrzech .
Liczba ludności Sopronu w 2011 roku wyniosła ponad 60,7 tys .
Miasto położone jest blisko granicy z Austrią , na północ od Alp Węgierskich i południe od Jeziora Nezyderskiego .
Miasto połączone jest liniami kolejowymi z Wiedniem , Budapesztem i Bratysławą .
Miasto znane jest z dobrych win .
Lokalnym specjałem są wina `` tramini '' i `` Soproni Kékfrankos '' .
Gospodarka .
W mieście rozwinął się przemysł włókienniczy , odzieżowy , metalowy , winiarski , mięsny oraz piwowarski .
Historia .
Osadnictwo na terenach Sopronu sięga czasów prehistorycznych .
Rzymska warownia `` Scarbantia '' została odbudowana w XI wieku przez francuskich krzyżowców .
