Serbskie skrzypce
Serbskie skrzypce ( `` serbske husle '' ) – tradycyjny serbołużycki ludowy instrument muzyczny .
Istnieją wywodzące się z regionu środkowych Łużyc skrzypce wielkie oraz pochodzące z okolic Slepo skrzypce małe , zwane też slepanskimi .
Skrzypce wielkie to typ skrzypiec dyszkantowych o trzech strunach , w dźwięku przypominających altówkę .
Struny napinane są za pomocą kołków umieszczonych pionowo od dołu na końcu szyjki .
Instrument ma około 50 cm długości , dolna płyta rezonansowa jest płaska , górna równomiernie wypukła i wgięta u nasady .
W czasie gry skrzypce trzymane są bokiem do lewej piersi , stabilność zapewnia podtrzymująca lewy łokieć taśma .
Skrzypce małe wywodzą się z instrumentu przeznaczonego pierwotnie do grania na weselach i wyglądem przypominają tradycyjne skrzypce .
W XVII i XVIII wieku upowszechniły się poza Łużyce ; znano je również na ziemiach polskich .
Trójstrunowy instrument ma około 24 cm długości i wydaje wysokie brzmienie .
Grano nam nim przy akompaniamencie małych dud zwanych `` měchawka '' .
