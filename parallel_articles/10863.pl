Flaga Serbołużyczan
Flaga Serbołużyczan – flaga narodowa Serbołużyczan .
Składa się z trzech jednakowych poziomych pasów w barwach pansłowiańskich : niebieskiej , czerwonej i białej ( licząc od góry ) .
Flagę w obecnych barwach przyjęto po kongresie wszechsłowiańskim , który w 1848 roku odbywał się w Pradze .
