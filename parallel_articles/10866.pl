Sordariomycetes
`` Sordariomycetes '' O.E .
Erikss .
& amp ; Winka – klasa workowców ( `` Ascomycota '' ) , której typem nomenklatorycznym jest `` Sordaria '' .
Jest to jedna z najliczniejszych w gatunki grup workowców .
Charakterystyka .
Workowce nie tworzące porostów .
Wytwarzają askokarpy przeważnie w formie perytecjum , rzadziej klejstotecjum , a worki są zazwyczaj 8-zarodnikowe , pozbawione wieka ( `` operculum '' ) i ułożone na podstawie lub na obwodzie hymenium .
Są szeroko rozpowszechnione i występują praktycznie we wszystkich ekosystemach .
