Soroki ( sielsowiet Obrub )
Soroki ( ; ) – wieś na Białorusi , w obwodzie witebskim , w rejonie głębockim , w sielsowiecie Obrub .
Leży pomiędzy drogą republikańską a linią kolejową Królewszczyzna – Łyntupy .
Historia .
W czasach zaborów w gminie Głębokie , w powiecie dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
W latach 1921–1945 wieś , osada i majątek leżały w Polsce , w województwie wileńskim , w powiecie dziśnieńskim , w gminie Głębokie .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało :
Wierni należeli do parafii rzymskokatolickiej i prawosławnej w Głębokiem .
Miejscowość podlegała pod Sąd Grodzki w Głębokiem i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Głębokiem .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
Od czerwca 1941 roku pod okupacją niemiecką .
W 1944 miejscowość została ponownie zajęta przez wojska sowieckie i włączona do Białoruskiej SRR .
Od 1991 w składzie niepodległej Białorusi .
W dwudziestoleciu międzywojennym leżały w Polsce , w województwie wileńskim , w powiecie dziśnieńskim , w gminie Głębokie .
