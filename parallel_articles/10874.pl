Sosna-Kozółki
Sosna-Kozółki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Suchożebry .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Marii Magdaleny w Suchożebrach .
Zaścianek szlachecki Kuzołki należący do okolicy zaściankowej Sosna położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
