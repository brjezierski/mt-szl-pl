Sosenki-Jajki
Sosenki-Jajki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mordy .
Składa się z 17 domostw .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Michała Archanioła w Mordach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Nazwa `` Jajki '' pochodzi od nieistniejącej obecnie drugiej części miejscowości .
Pierwotna nazwa wsi brzmiała Sosniny Jajky .
Znajduje się tutaj jedno gospodarstwo agroturystyczne .
W okresie powojennym liczni młodzi mieszkańcy wioski podjęli pracę w PKP na Ziemiach odzyskanych ( rejon Wrocławia ) .
Najpopularniejsze nazwiska : Soseński , Soszyński , Szymański , Kobyliński , Płoskowicz .
Dwa ostatnie rody obecnie już w Sosenkach nie mieszkają .
Na terenie wsi znajdował się niegdyś dworek .
Wieś leży na granicy dwóch obwodów łowieckich .
Tereny obfitują przede wszystkim w dziki .
W ostatnich latach pojawia się coraz więcej łosi oraz jeleni .
