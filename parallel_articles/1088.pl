Alfred Nobel
Alfred Bernhard Nobel ( ur .
21 października 1833 w Sztokholmie , zm .
10 grudnia 1896 w San Remo ) – szwedzki chemik , inżynier , przemysłowiec , wynalazca m.in .
dynamitu , fundator Nagrody Nobla .
Życiorys .
Był synem Immanuela Nobla i Karoliny Ahlsell .
Para pobrała się w 1827 i miała ośmioro dzieci , z których wieku dorosłego dożyli tylko Alfred i jego trzech braci .
Ojciec Nobla , inżynier i wynalazca , po niepowodzeniach biznesowych przeniósł się w 1837 do Sankt Petersburga .
Odniósłszy tam sukces na polu produkcji materiałów wybuchowych i maszyn , w 1842 ściągnął tam swoją rodzinę .
Alfred pobierał tam nauki u prywatnych nauczycieli .
W wieku 16 lat biegle władał językami angielskim , francuskim , niemieckim i rosyjskim .
W 1850 udał się do Paryża , gdzie spędził rok studiując chemię ; następnie przeniósł się do Stanów Zjednoczonych , gdzie przez cztery lata pracował pod kierunkiem Johna Ericssona .
Po powrocie do stolicy Rosji podjął pracę w należącej do ojca fabryce wytwarzającej sprzęt wojskowy na potrzeby wojny krymskiej .
Gdy w 1856 wojna dobiegła końca , fabryka miała trudności z przestawieniem się na produkcję pokojową i zbankrutowała w 1859 .
