Wólka Soseńska
Wólka Soseńska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mordy .
Wierni Kościoła rzymskokatolickiego należą do parafii Parafia św .
Michała Archanioła w Mordach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
