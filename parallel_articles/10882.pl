Soundgarden
Soundgarden – amerykański zespół muzyczny utworzony w 1984 w Seattle w stanie Waszyngton z inicjatywy wokalisty Chrisa Cornella , gitarzysty Kima Thayila i basisty Hiro Yamamoto .
Wraz z Green River uznawany za jednego z głównych prekursorów grunge ’ u , powstałego w połowie lat 80. podgatunku rocka alternatywnego .
Przez historyków i krytyków zespół zalicza się współcześnie do tzw .
Wielkiej Czwórki z Seattle , obok Alice in Chains , Nirvany i Pearl Jam .
Muzyka Soundgarden czerpała inspirację z tradycji zespołów garażowych lat 60. , szeroko pojętego rocka psychodelicznego i heavy metalu lat 70. , m.in .
spod znaku Black Sabbath , oraz surowej estetyki punk rocka lat .
80 .
