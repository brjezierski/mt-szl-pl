Alfred Zaręba
Alfred Zaręba ( ur .
10 listopada 1921 w Krakowie , zm .
20 lutego 1988 tamże ) – polski językoznawca , slawista , polonista , dialektolog .
Biografia .
W 1939 ukończył Gimnazjum im .
B. Nowodworskiego w Krakowie .
Podczas II wojny światowej jego ojciec zginął w Auschwitz .
Studia polonistyczne i slawistyczne Zaręba podjął na tajnym Uniwersytecie Jagiellońskim w r. 1942 , ukończył je w 1946 , stopień doktora nauk humanistycznych uzyskał w 1951 r. Był lektorem języka polskiego w Lund ( Szwecja ) .
Od 1955 r. docent , od 1965 r. profesor .
Od 1955 r. kierownik Zakładu Dialektologii Słowiańskiej na Uniwersytecie Jagiellońskim .
Główną dziedziną jego pracy naukowej była dialektologia , a także onomastyka .
Najważniejszą pracą jest `` Atlas językowy Śląska '' ( t. 1–7 ) .
Inne ważniejsze publikacje : `` Nazwy barw w dialektach języka polskiego '' ( 1954 ) , `` Orawskie teksty gwarowe z obszaru Polski i Czechosłowacji '' ( 1967 ) , `` Polskie imiona ludowe '' , `` Słownik starych Siołkowic w pow .
opolskim '' ( 1960 ) .
Był promotorem dysertacji doktorskiej Romana Laskowskiego pt .
Derywacja rzeczowników w dialektach laskich '' .
Razem ze swoją żoną Marią Zarębiną wydał książki o tajnym nauczaniu .
Pochowany na Cmentarzu Rakowickim w Krakowie .
