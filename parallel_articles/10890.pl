South Shields
South Shields – miasto w północno-wschodniej Anglii ( Wielka Brytania ) , w hrabstwie Tyne and Wear ( historycznie należało do hrabstwa Durham ) , w dystrykcie South Tyneside .
South Shields znajduje się ok. 8 km na południowy wschód od Newcastle upon Tyne , przy ujściu rzeki Tyne do Morza Północnego .
Trzecie co do wielkości miasto w zespole miejskim Tyneside , po Newcastle i Gateshead .
W 2011 roku liczyło 75 337 mieszkańców .
Dawniej ośrodek eksploatacji węgla kamiennego ; przemysł stoczniowy , maszynowy , metalowy , chemiczny ; port handlowy ( eksport węgla ) .
Miejsce urodzenia brytyjskiego reżysera Ridleya Scotta , oraz piosenkarek Perrie Edwards i Jade Thirlwall ( Little Mix )
