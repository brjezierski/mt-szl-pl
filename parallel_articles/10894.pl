Czarkówka jednobarwna
Czarkówka jednobarwna ( `` Sowerbyella unicolor '' ( Gillet ) Nannf . )
– gatunek grzybów z rodziny `` Pyronemataceae ''
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sowerbyella , Pyronemataceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1879 r. Gillet nadając mu nazwę `` Aleuria unicolor '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1938 r. Nannfeldt , przenosząc go do rodzaju `` Sowerbyella '' .
Synonimy nazwy naukowej :
Nazwę polską podają niektóre atlasy grzybów .
Morfologia .
Owocnik za młodu kubeczkowaty , później rozpostarty , o brzegu wygiętym w dół lub w górę ; wewnątrz jaskrawy , pomarańczowożółty , z zewnątrz bladokremowy i delikatnie otrębiasty ; trzoneczek centralny , korzonkowaty , biało-filcowaty ; do 4 cm średnicy .
Występowanie i siedlisko .
Jesienią , przeważnie w gęstych grupach w lasach iglastych .
Gatunki podobne .
Równie rzadka czarkówka korzonkowata ( `` Sowerbyella radicata '' ) ma trzon ciemny , szary i włókienkowaty ; można ją rozpoznać tylko pod mikroskopem .
Prószyczka żółta ( `` Rustroemia bolaris '' ) tworzy swoje małe owocniki na martwym drewnie grabu i olszy zielonej .
Są one mniejsze , do 10 mm średnicy , pojawiają się w lecie .
