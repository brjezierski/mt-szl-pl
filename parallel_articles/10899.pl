Sozopol
Sozopol ( , , ) – bułgarskie miasto położone 30 kilometrów na południe od Burgasu na południowym wybrzeżu bułgarskiego Morza Czarnego ; w obwodzie Burgas .
Siedziba administracyjna gminy Sozopol .
Według danych Narodowego Instytutu Statystycznego w Bułgarii z 31 grudnia 2011 miasto liczyło 4285 mieszkańców .
Miasto jest obecnie znanym kurortem oraz miejscem , w którym odbywa się festiwal filmowy nazywany `` Apollonią '' od jednej ze starożytnych nazw Sozopola .
Historia .
Sozopol jest jednym z najstarszych miast na bułgarskim wybrzeżu Morza Czarnego .
Pierwsza wzmianka o Sozopolu pochodzi sprzed epoki brązu .
Badania w okolicach Sozopola wykazały pozostałości dawnych mieszkań , porcelanowych garnków , kamieni i narzędzi wykonanych z kości , pochodzących z tamtej epoki .
