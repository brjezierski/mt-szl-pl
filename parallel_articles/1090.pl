Alfred Znamierowski
Alfred Znamierowski , pseudonim „ Jacek Gryf ” ( ur .
21 czerwca 1940 w Warszawie , zm .
23 października 2019 w Pradze ) – polski dziennikarz , heraldyk i weksylolog .
Życiorys .
Jest wnukiem Czesława Znamierowskiego .
Studiował geografię na Uniwersytecie Warszawskim .
W pierwszej połowie lat 60. publikował artykuły o flagach i banderach m.in .
w „ Dookoła Świata ” , „ Skrzydlata Polska ” , „ Morze ” , „ Kontynenty ” .
Następnie emigrował , w 1966 uzyskał azyl polityczny we Francji .
W latach 1966–1978 pracował w monachijskim Radio Wolna Europa , w 1985–1994 w sekcji polskiej Głosu Ameryki , m.in .
jako jej korespondent w Rzymie .
Współpracował z paryską Kulturą i innymi pismami emigracyjnymi .
Był członkiem najwyższych władz Ruchu Społeczno-Politycznego POMOST i jego przedstawicielem w Waszyngtonie , członkiem Solidarności Walczącej .
W Niemczech współpracował z niemieckim heraldykiem Ottfriedem Neubeckerem , będąc wykonawcą ilustracji – rysunków herbów do jego leksykonu Wappen Bilder–Lexikon oraz encyklopedii Brockhaus Encyklopädie .
W 1978 założył w San Diego ( Stany Zjednoczone ) ośrodek The Flag Design Center , zajmujący się projektowaniem flag dla miast , hrabstw oraz osób prywatnych , a także badaniami weksylologicznymi .
Następnie został dyrektorem artystycznym w Annin & amp ; Co w New Jersey , przedsiębiorstwie zajmującym się produkcją flag .
Równocześnie był też głównym ilustratorem amerykańskiego pisma The Flag Bulletin .
Po przemianach roku 1989 w Polsce powrócił do kraju .
W 1992 był współzałożycielem Polskiego Towarzystwa Weksylologicznego .
Wykonuje ilustracje przedstawiające flagi i herby dla czasopism oraz wydawnictw , w tym dla PWN .
W 1997 założył Instytut Heraldyczno-Weksylologiczny , zajmujący się badaniami herbów i flag oraz projektujący znaki dla polskich jednostek samorządowych .
Członek Polskiego Towarzystwa Heraldycznego , Komisji Heraldycznej przy Ministerstwie Spraw Wewnętrznych i Administracji i Rady Numizmatycznej przy NBP , w latach 1998–2000 prezes , następnie wiceprezes PTW .
Odznaczenia .
15 czerwca 2007 roku został odznaczony Złotym Krzyżem Zasługi za zasługi w dokumentowaniu prawdy historycznej o dziejach Narodu Polskiego .
28 lipca 2016 otrzymał Krzyż Oficerski Orderu Odrodzenia Polski za wybitne zasługi na rzecz przemian demokratycznych w Polsce .
W 2016 został wyróżniony Krzyżem Solidarności Walczącej .
