Soćki
Soćki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wodynie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckim
Wierni Kościoła rzymskokatolickiego należą do parafii Wniebowzięcia Najświętszej Maryi Panny w Stoczku Łukowskim .
We wsi znajduje się założona w 1967 roku jednostka ochotniczej straży pożarnej .
Jest to jednostka typu M , czyli nie posiadająca samochodu , a jedynie motopompę wożoną na pojeździe zastępczym .
Historia .
Miejscowość istniała już w XVI w. Początkowo była zaściankiem rodu Soćków , skąd wzięła swoją nazwę .
Historycznie znajduje się w granicach Mazowsza w granicach Ziemi Czerskiej , w powiecie Latowickim .
W 1660 roku wieś wchodziła w skład starostwa Latowicz .
Kmiecie z tego rejonu nie odrabiali pańszczyzny , lecz płacili dodatkowy podatek .
W 1876 roku we wsi było 13 domów i 111 mieszkańców , obszar wynosił 302 morgi .
W roku 1876 znajdował się tu również tartak parowy .
