SV Ratibor 03
SV Ratibor 03 – górnośląski klub piłkarski utożsamiany z przedwojenną społecznością niemiecką miasta Racibórz .
Powstał w 1903 roku w Ratibor ( obecny Racibórz ) w II Rzeszy .
Był pierwszym zarejestrowanym klubem piłkarskim na Górnym Śląsku .
Obecnie nie istnieje .
Historia .
Do 1911 nosił nazwę FC Ratibor .
Założycielem klubu był Fritz Seidl .
W sezonie 1933/1934 klub w `` Gauliga Schlesien '' ( śląskiej lidze okręgowej – najwyższej klasie rozgrywkowej w ówczesnej strukturze rozgrywek piłkarskich w Rzeszy ) zajął 6. miejsce , w sezonie 1934/1935 uplasował się na 4. pozycji , w sezonie 1935/1936 na 7. pozycji , w sezonie 1936/1937 miał ostatnie miejsce .
Klub powrócił do `` Gauligi Schlesien '' w sezonie 1938/1939 i zajął 7. miejsce .
