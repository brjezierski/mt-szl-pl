Spartakus ( film 1960 )
Spartakus ( ang .
`` Spartacus '' ) – amerykański film historyczno-kostiumowy z 1960 roku w reżyserii Stanleya Kubricka .
Ekranizacja powieści Howarda Fasta pod tym samym tytułem , opisującej przebieg największego w dziejach starożytnego Rzymu powstania gladiatorów i niewolników .
Film nagrodzony czterema Oscarami .
Treść .
Epicka opowieść o powstaniu niewolników w starożytnym Rzymie ( 73-71 p.n.e .
) , ukazana poprzez losy jego przywódcy – legendarnego Traka Spartakusa .
Jako niewolnik skazany na pracę w kamieniołomach , atakując brutalnego strażnika zwraca na siebie uwagę rzymskiego właściciela szkoły gladiatorów , Lentulusa Batiatusa .
Lanista wysyła go do swej posiadłości w Kapui , gdzie pod kierunkiem bezwzględnego Marcellusa trenuje wraz z innymi rozmaite metody walki .
Tam zakochuje się również w usługującej im niewolnicy Varinii .
Gdy wśród gladiatorów wybucha bunt , Spartakus staje na jego czele .
Miejscowa rewolta wkrótce ogarnia całą Kampanię , a następnie inne obszary państwa Rzymian , przyciągając masy zbiegłych niewolników i ludności zależnej .
Senat wysyła przeciw nim legiony , które zostają doszczętnie rozbite .
Ale próby opuszczenia Italii przez zbuntowanych z pomocą cylicyjskich piratów kończą się niepowodzeniem wskutek ich zdrady .
Tymczasem Rzym wysyła przeciw powstańcom potężne siły pod wodzą doświadczonego Marka Krassusa…
„ Ja jestem Spartakusem ! ” .
Do historii kina przeszła zwłaszcza scena po decydującej bitwie przegranej przez armię niewolników .
Spartakus zostaje wprawdzie schwytany ( w rzeczywistości najprawdopodobniej zginął ) , jednak Rzymianie , nie znając jego wyglądu , nie są w stanie go rozpoznać .
Dlatego pojmanym niewolnikom proponują , że nie zostaną ukrzyżowani , jeśli tylko wskażą swego wodza .
Kiedy w tej sytuacji Spartakus dobrowolnie chce się ujawnić , jego ocalali współtowarzysze też powstają ze słowami „ Ja jestem Spartakusem ! ” – tym samym wybierając okrutną śmierć zamiast zdrady .
