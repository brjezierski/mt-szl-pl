Agaricus litoralis
Agaricus litoralis ( Wakef .
& amp ; A. Pearson ) Pilát – gatunek grzybów należący do rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował Persoon , potem Elsi Maud Wakefield , który w 1946 r. nadał mu nazwę `` Psaliota litoralis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1952 r. Albert Pilát , przenosząc go do rodzaju `` Agaricus '' .
Charakterystyka .
Saprotrofy wytwarzające jadalne owocniki z białawym , w centralnej części lekko żółtawym lub brązowawym , pokrytym przylegającymi łuskami kapeluszem ( średnicy 4–8 cm ) o blaszkowatym hymenoforze , umieszczonym na maczugowatym trzonie , do którego przyrośnięty jest wąski , zwisający pierścień .
Charakterystyczną cechą `` Agaricus litoralis '' jest obecność cystyd na ostrzach blaszek .
Gatunek ten występuje na łąkach , obrzeżach lasów , czasami w zaroślach , owocniki wytwarza od lipca do września .
