Spira
Spira ( niem .
`` Speyer '' ) – miasto na prawach powiatu w Niemczech , położone nad Renem w kraju związkowym Nadrenia-Palatynat .
Jedno z najstarszych miast Niemiec , na początku swojego istnienia nosiło nazwę `` Civitas Nemetum '' .
Położenie .
Miasto leży na obszarze Niziny Górnoreńskiej w regionie Rhein-Neckar-Odenwald należącym do kraju związkowego Nadrenia-Palatynat .
