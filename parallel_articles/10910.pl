Sphacelotheca reiliana
Sphacelotheca reiliana ( J.G .
Kühn ) G.P .
Clinton – gatunek grzybów z klasy `` Microbotryomycetes '' .
Pasożyt kukurydzy i sorga , wywołujący u nich chorobę o nazwie głownia pyląca kukurydzy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sphacelotheca , Microbotryaceae , Microbotryales , incertae sedis , Microbotryomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1875 r. Julius Kühn nadając mu nazwę `` Ustilago reiliana '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1902 r. George Perkins Clinton , przenosząc go do rodzaju `` Sphacelotheca '' .
Niektóre synonimy nazwy naukowej :
Rozwój .
Na wiechach i kolbach zaatakowanych roślin pojawiają się telia wypełnione brunatnoczarnymi teliosporami .
Porażone mogą być całe wiechy i kolby , lub tylko ich części .
Kuliste teliospory mają średnicę 9–12 μm .
Zimują w ziemi , lub w ziarniakach pozostawionych na polu jako resztki pożniwne , a także tych przeznaczonych do siewu .
Po przejściu okresu spoczynku kiełkują i tworzą 4-komórkowe podstawki .
W każdej z 4 komórek podstawki powstaje sporydium o haploidalnej liczbie chromosomów .
Gdy spotkają się dwa różne płciowo sporydia zachodzi między nimi plazmogamia .
W jej wyniku powstaje dwujądrowa komórka wytwarzająca również dwujądrową strzępkę infekcyjną , która wrasta do kiełka siewki kukurydzy lub sorga i dalej rozrasta się w zainfekowanej roślinie wytwarzając teliospory .
