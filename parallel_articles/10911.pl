Sphaleromyces lathrobii
Sphaleromyces lathrobii Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Sphaleromyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1894 r. Roland Thaxter na owadach `` Lathrobium nitidulum '' i `` Lathrobium puntulatum '' w USA .
Synonim : `` Corethromyces lathrobii '' ( Thaxt . )
Thaxt .
1912 .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski opisał jego występowanie na chrząszczach `` Lathrobium terminatum '' z rodziny kusakowatych ( Staphylinidae ) .
