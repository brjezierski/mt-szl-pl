Szpileczka czerniejąca
Szpileczka czerniejąca ( `` Spinellus fusiger '' ( Link ) Tiegh . )
– gatunek grzybów należący do typu `` Mucoromycota '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phycomycetaceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1824 r. Johann Heinrich Friedrich Link nadając mu nazwę `` Mucor fusiger '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1875 r. Philippe Édouard Léon Van Tieghem .
Synonimy :
Morfologia i rozwój .
Po zainfekowaniu grzyba przez zarodniki `` Spinellus fusiger '' we wnętrzu jego owocnika rozwija się grzybnia , która na kapeluszu grzyba wytwarza proste , wzniesione , przezroczyste i bezbarwne trzonki zwane sporangioforami .
Mają długość 1–3 cm .
Na szczycie sporangioforów rozwijają się kuliste , bezbarwne zarodnie ( sporangia ) .
