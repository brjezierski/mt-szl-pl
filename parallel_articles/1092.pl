Działanie algebraiczne
Działanie algebraiczne ( operacja algebraiczna ) – przyporządkowanie jednemu lub większej liczbie elementów ( nazywanych `` argumentami '' lub `` operandami '' ) jednego elementu ( nazywanego `` wynikiem '' ) .
Argumentami i wynikami działań mogą być dowolne obiekty matematyczne : liczby ( skalary ) , wektory , macierze , tensory , algebry , zdania logiczne , funkcje itp .
Do podstawowych działań algebraicznych należą tradycyjne działania arytmetyczne ( tj .
działania na liczbach ) , jak dodawanie , odejmowanie , mnożenie , dzielenie , podnoszenie do potęgi , pierwiastkowanie .
Działania te – odpowiednio zdefiniowane – mogą być wykonane także na macierzach , wyrażeniach algebraicznych , czy na innych elementach struktury algebraicznej , jak grupy czy pola .
Działaniem algebraicznym jest też obliczanie iloczynu skalarnego , obliczanie potęgi całkowitej i wymiernej .
Działaniem algebraicznym nie jest zaś np .
obliczanie pochodnej funkcji .
