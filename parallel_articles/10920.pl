Sporophagomyces chrysostomus
Sporophagomyces chrysostomus ( Berk .
& amp ; Broome ) K. Põldmaa & amp ; Samuels – gatunek grzybów z klasy `` Sordariomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sporophagomyces , Hypocreaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1873 r. Miles Joseph Berkeley i Christopher Edmund Broome , nadając mu nazwę `` Hypomyces chrysostomus '' .
Obecną nazwę nadali mu K. Põldmaa i G.J .
Samuels w 2000 r .
Ma 7 synonimów .
Niektóre z nich :
Łacińska nazwa rodzajowa `` Sporphagomyces '' w tłumaczeniu na polski znaczy `` żywiący się zarodnikami '' .
Morfologia .
Na podłożu tworzy pierzastą grzybnię o średnicy do około 3,5 cm .
Powierzchnia szczotkowata , filcowata i lśniąca , trochę gąbczasta , początkowo biała , potem od zarodników zabarwiona na brązowo .
Perytecja jajowate , początkowo bladożółte , następnie oliwkowe do czerwonawych , z dużym ostiolum .
Zarodniki zakrzywione , o ostrym lub tępym wierzchołku , gładkie , ze środkową przegrodą , 10–15 × 3–4 µm .
`` Sporophagomyces chrysostomus '' rozwija się na owocnikach innych grzybów , odżywiając się ich zarodnikami .
Przyczepiony jest do żywiciela tylko środkową częścią swojej grzybni , żywiciela , podczas gdy filcowata jej reszta zwisa poniżej hymenofor żywiciela i zatrzymuje jego zarodniki , które następnie trawi .
Występowanie i siedlisko .
Znane jest występowanie `` Sporophagomyces chrysostomus '' głównie w Ameryce Północnej i Europie , pojedyncze stanowiska podano także w Ameryce Południowej i Azji .
W Polsce po raz pierwszy jego stanowiska podano w 2019 i 2020 r. Bardziej aktualne stanowiska podaje internetowy atlas grzybów .
Znajduje się w nim na liście gatunków zagrożonych , wartych objęcia ochroną .
Grzyb pasożytniczy występujący na owocnikach grzybów z rodzaju `` Ganoderma '' ( lakownica ) , `` Rigidoporus '' ( twardoporek ) i `` Phellinopsis '' .
