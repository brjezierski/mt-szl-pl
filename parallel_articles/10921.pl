Morfologia .
Formy wolno żyjące występują w glebie .
Kolonie grzyba rozrastają się umiarkowanie szybko w temperaturze 25 °C .
Są one wilgotne , posiadają drobno pofałdowaną powierzchnię .
Ich kolor początkowo jest biały , z czasem staje się kremowy do ciemnego brązu .
W temperaturze 37 °C , kolonie są podobne do drożdży i kremowe .
Kolor od kremowego do beżu .
Znaczenie .
