Spycimierz
Spycimierz ( lub `` Spicymierz '' , `` Spicimierz '' ) – wieś ( dawniej miasto ) w Polsce położona w województwie łódzkim , w powiecie poddębickim , w gminie Uniejów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa konińskiego .
Spycimierz uzyskał lokację miejską około 1357 roku , zdegradowany po 1450 roku .
Wieś arcybiskupstwa gnieźnieńskiego w powiecie szadkowskim województwa sieradzkiego w końcu XVI wieku .
Nazwa .
Nazwa miejscowości pochodzi od męskiego imienia słowiańskiego `` Spycimir '' , które występuje również w formie `` Spycimierz '' .
Miejscowość w staropolskiej formie `` Spicimir '' notuje Gall Anonim w swojej Kronice polskiej spisanej w latach 1112–1116 .
Historia .
Wieś położona na południe od Uniejowa .
Istniał tu gród , wspomniany w kronice Galla Anonima pod r. 1107 , zbudowany na miejscu , w którym krzyżowały się szlaki piesze : z Pomorza na Ruś oraz z Łęczycy do Kalisza .
Był to obiekt pierścieniowaty , o stosunkowo niewielkich rozmiarach .
Urządzenia obronne składały się z wału drewniano-ziemnego o konstrukcji rusztowej , zwieńczonego drewnianym ostrokołem .
W grodzie tym w toku walk ze Zbigniewem Bolesław Krzywousty więził arcybiskupa Marcina , stronnika swego brata .
Warownia spicymierska była siedzibą kasztelana i przetrwała w tym charakterze do początków XIV stulecia , czyli do przejścia grodu w prywatne ręce .
