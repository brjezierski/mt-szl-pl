Algier
Algier ( fr .
`` Alger '' , arab .
مدينة الجزائر [ `` Madinat al-Dżaza'ir '' ] ) – stolica Algierii .
Miasto leży w obszernej zatoce nad Morzem Śródziemnym , 800 km od Marsylii .
Nad miastem wznoszą się wzgórza do wysokości 400 m n.p.m .
Algier jest centrum gospodarczym , kulturalnym i głównym portem kraju , a także najważniejszym ośrodkiem naukowym .
Położenie .
Miasto leży na stokach Sahelu rozciągających się wzdłuż zatoki Algierskiej .
Gospodarka .
Posiada największy port handlowy kraju .
Transport .
Metro w Algierze jest jedynym obok kairskiego na kontynencie afrykańskim .
Historia .
W starożytności nosił nazwę Icosium .
