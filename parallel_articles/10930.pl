Srbobran ( serb .
Србобран , węg .
`` Szenttamás '' , niem .
`` Thomasberg '' ) – miasto w Serbii , w Wojwodinie , w okręgu południowobackim , siedziba gminy Srbobran .
Leży w regionie historycznym Baczka .
W 2011 roku liczyło 12 009 mieszkańców .
W mieście rozwinął się przemysł materiałów budowlanych oraz spożywczy .
