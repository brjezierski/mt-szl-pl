Srebrna Góra ( Bielany )
Srebrna Góra ( 317 m n.p.m . )
– czwarte pod względem wysokości a trzecie pod względem wybitności wzgórze na terenie zrębu Pasma Sowińca na terenie Lasu Wolskiego w Krakowie , w jego południowo-zachodniej krańcu .
Znajduje się w końcowej części grzbietu odchodzącego ku południowi od Ostrej Góry , oddzielone od niego szeroką Bielańską Przełęczą z Polaną pod Dębiną zwaną też Polaną Bielańską ( wys .
ok. 285 m ) .
Od wschodu opada do głęboko wciętego wąwozu Łupany Dół , a ku południu do doliny Wisły .
Na stromych , południowych stokach znajduje się Rezerwat przyrody Bielańskie Skałki .
Na szczycie wzgórza znajduje się Klasztor Kamedułów i Kościół Wniebowzięcia Najświętszej Maryi Panny .
Tuż obok klasztoru znajduje się Bateria FB-35 „ Srebrna Góra ” należąca do zespołu dzieł obronnych Twierdzy Kraków .
Na południowych i południowo-zachodnich stokach Srebrnej Góry , po obu stronach alei Wędrowników , u podnóża klasztoru kamedułów , znajduje się druga co do wielkości w Polsce winnica .
Na Srebrnej Gorze znajduje się Schronisko pod wsią Bielany oraz wykuta przez Austriaków Kawerna Bielany .
