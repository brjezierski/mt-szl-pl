Srebrna ( województwo łódzkie )
Srebrna – osada w Polsce położona w województwie łódzkim , w powiecie łęczyckim , w gminie Grabów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa konińskiego .
Według danych za rok 2006 miejscowość zamieszkuje 180 osób .
Historia .
Pierwsza wzmianka znajdująca się w Księgach sądowych łęczyckich pochodzi z 1394 roku .
Osada lokowana była na prawie niemieckim i należała do dziedziców z Grabowa .
U schyłku XV wieku przeszła w posiadanie Sokołowskich z Wrzący h. Pomian .
W 2 połowie XVI wieku nadal pozostawała w ich rękach , liczyła 12,5 łana kmiecego .
