Сримска Митровица , Srymska Mytrowyca ) – miasto w Serbii , w Wojwodinie , stolica okręgu sremskiego , siedziba miasta Sremska Mitrovica .
Leży na lewym brzegu Sawy .
W 2011 roku liczyło 41 624 mieszkańców .
Historia .
W okresie panowania rzymskiego funkcjonowała jako Sirmium .
Opuszczone w 582 roku wskutek ekspansji Kaganatu Awarów , wkrótce spłonęło .
W VII-IX wieku pod władzą chanów Protobułgarów – Kubera , a wkrótce później Kruma .
Na początku XI wieku Sirmium wróciło pod władanie Cesarstwa Bizantyńskiego .
W 1167 roku miała miejsce bitwa pod Sirmium , a w 1180 roku miasto zostało przyłączone do Królestwa Węgier .
Przyjęto następnie nazwę „ Civitas Sancti Demetrii ” .
Następnie nazwa została zmieniona na „ Dmitrovica ” , „ Mitrovica ” , a ostatecznie przyjęto obecną nazwę – „ Sremska Mitrovica ” .
Gospodarka .
W mieście rozwinął się przemysł drzewny , celulozowo-papierniczy , włókienniczy oraz spożywczy .
