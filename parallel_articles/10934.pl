Sri Lanka
Sri Lanka ( syng .
ශ්රී ලංකාව , tamil .
இலங்கை , ang .
`` Sri Lanka '' ) , oficjalnie Demokratyczno-Socjalistyczna Republika Sri Lanki ( , , ang .
`` Democratic Socialist Republic of Sri Lanka '' ) – państwo w Azji Południowej , na wyspie Cejlon ( pod tą nazwą znane do 1972 ) wraz z mniejszymi przybrzeżnymi wyspami .
Oddzielone od Półwyspu Indyjskiego Cieśniną Palk i Zatoką Mannar .
Od wschodu oblewane przez Zatokę Bengalską , od południa otwartym Oceanem Indyjskim .
Największe miasta kraju to : Kolombo , Dehiwala , Moratuwa , Dżafna , Kandy , Galle , Kalmunai .
Ustrój polityczny .
Sri Lanka jest demokratyczno-socjalistyczną republiką w ramach Wspólnoty Narodów .
Zgodnie z konstytucją z 16 sierpnia 1978 głową państwa jest prezydent , który stoi na czele rządu i jest naczelnym dowódcą armii .
Wybierany jest w wyborach powszechnych maksymalnie na dwie 6-letnie kadencje .
Prezydent , spośród członków parlamentu , powołuje premiera i w konsultacji z nim ministrów rządu .
Premier zastępuje prezydenta we wszystkich przypadkach uniemożliwiających pełnienie przez niego swojej funkcji .
Władza ustawodawcza należy do jednoizbowego parlamentu , do którego wybieranych jest 225 deputowanych na 6-letnią kadencję .
Oficjalną stolicą kraju jest Sri Dźajawardanapura Kotte ( znane również jako Kotte ) , do którego przeniesiono parlament w celu zdecentralizowania Kolombo .
Wciąż jednak większość instytucji centralnych znajduje się w Kolombo , z którego będą w przyszłości przenoszone .
Z racji tego , że Kotte jest faktycznie jednym z przedmieść Kolombo , zwykle nie jest traktowane jako osobna aglomeracja .
Nazwa .
Sri Lanka .
Nazwa państwa Sri Lanka , przyjęta w 1972 ( wcześniej jako Cejlon ) , oznacza w sanskrycie „ olśniewający kraj ” .
Obecna nazwa jest współczesną adaptacją nazwy występującej w Ramajanie , gdzie wyspa znana jest jako `` Lanka '' .
W Ramajanie wyspa znana jest także jako `` Lankadweepa '' ( dwepa – wyspa ) oraz `` Lakdiva '' ( diva – wyspa ) .
