Srock
Srock ( daw .
`` Srocko '' ) – wieś w Polsce położona w województwie łódzkim , w powiecie piotrkowskim , w gminie Moszczenica .
Do 1953 roku miejscowość była siedzibą gminy Podolin , a w latach 1953–1954 gminy Srocko .
W latach 1975–1998 miejscowość administracyjnie należała do województwa piotrkowskiego .
