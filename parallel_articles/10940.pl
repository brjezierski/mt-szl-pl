Sankt Anton am Arlberg
Sankt Anton am Arlberg , St. Anton am Arlberg , Sankt Anton , St. Anton – gmina położona na zachodzie Austrii , w kraju związkowym Tyrol , w powiecie Landeck .
Leży w Alpach Lechtalskich , poniżej ujścia Moosbachu do Rosanny , blisko granicy z krajem związkowym Vorarlberg .
Jeden z największych ośrodków narciarskich tego regionu Austrii .
Historia .
Miejscowość rozwinęła się z przystanku na trakcie przez przełęcz Arlberg .
Od 1824 przez przełęcz prowadziła utwardzona droga , a w latach 1880-1884 wybudowano linię kolejową ( Arlbergbahn ) , przez dłuższy czas jedyne połączenie zimą na zachód .
W Sankt Anton am Arlberg rozegrane zostały Mistrzostwa Świata w Narciarstwie Alpejskim w 2001 roku .
W ramach przygotowań poczyniono liczne inwestycje infrastrukturalne , m.in .
wybudowano nowe obiekty sportowe i przeniesiono odcinek linii kolejowej poza centrum miejscowości .
Według danych z 1 stycznia 2015 Sankt Anton am Arlberg jest zamieszkiwane przez 2417 osób .
Powierzchnia miejscowości wynosi 165,81 km² .
Gmina leży na wysokości 1284 m n.p.m ..
