St. Johann in Tirol
St. Johann in Tirol , Sankt Johann in Tirol – gmina targowa w Austrii , w kraju związkowym Tyrol , w powiecie Kitzbühel .
Jest bazą narciarską i ośrodkiem sportowym .
Liczy 9036 mieszkańców ( 1 stycznia 2015 ) .
Leży na granicy dwóch pasm górskich Kaisergebirge i Alp Kitzbühelskich .
W przeszłości często odbywały się tu zawody w ramach Pucharu Świata w narciarstwie dowolnym .
