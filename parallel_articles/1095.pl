Algirdas Brazauskas
Algirdas Mykolas Brazauskas ( ur .
22 września 1932 w Rakiszkach , zm .
26 czerwca 2010 w Wilnie ) – litewski polityk , w okresie ZSRR działacz komunistyczny , prezydent Litwy w latach 1993–1998 , premier Litwy w latach 2001–2006 .
Życiorys .
Ukończył w 1956 studia inżynieryjne w Kowieńskim Instytucie Politechnicznym .
Obronił później pracę z zakresu ekonomii w CEMI ( instytucie ekonomiczno-matematycznym Akademii Nauk ZSRR w Moskwie ) , uzyskując w 1974 stopień doktora .
Po ukończeniu studiów podjął pracę w budownictwie .
W latach 1956–1957 był głównym inżynierem na budowie hydroelektrowni kowieńskiej , a od 1958 do 1962 naczelnikiem zarządu przedsiębiorstwa budownictwa energetycznego .
Od 1962 kierował wydziałem materiałów budowlanych w Radzie Gospodarki Narodowej Litewskiej SRR .
W latach 1965–1966 był ministrem przemysłu materiałów budowlanych , po czym został zastępcą przewodniczącego Państwowego Komitetu Planowania .
Pełnił tę funkcję do 1977 .
