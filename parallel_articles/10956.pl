Stachybotrys chartarum
Stachybotrys chartarum – gatunek grzyba z rzędu rozetkowców ( `` Hypocreales '' ) .
Jest to czarna pleśń mająca szkodliwy wpływ na zdrowie człowieka .
Gatunek ten wzbudził duże zainteresowanie , ponieważ wytwarza jedne z najsilniejszych znanych mykotoksyn .
Istnieją dwa toksyczne chemotypy `` S.chartarum '' .
Pierwszy bardziej toksyczny zawierający makrocykliczne trichoteceny i drugi wytwarzający atranony oraz proste trichoteceny , które nie są makrocykliczne .
Do zakażenia trichotecenami może dochodzić drogą pokarmową , oddechową bądź przez skórę .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Stachybotryaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi ''
Po raz pierwszy opisał go w 1818 r. Christian Gottfried Ehrenberg nadając mu nazwę `` Stilbospora chartarum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Stnaley John Hughes w 1958 r .
