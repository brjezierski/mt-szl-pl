Stadion MOSiR Rybnik
Stadion MOSiR Rybnik może pomieścić 10 304 osoby .
Wszystkie miejsca są siedzące .
Stadion jest oświetlony lampami o natężeniu 1200 luksów .
Opis obiektu .
Stadion miejski , na którym rozgrywa swe mecze drużyna piłkarska oraz rybnicka drużyna żużlowa , jest częścią kompleksu sportowo – rekreacyjnego , położonego na peryferiach miasta .
W jego skład , poza stadionem piłkarsko-żużlowym , wchodzi stadion lekkoatletyczny , kąpielisko `` Ruda '' oraz boiska treningowe sekcji piłkarskiej .
Pierwsze zawody na obecnym w stadionie w Rybniku przeprowadzono w czerwcu 1939 roku na bieżni wyłożonej mączką ceglaną .
Na przełomie wieków stadion został zmodernizowany .
Najpierw , zimą 2000 roku przebudowano tor – zmianie uległa jego geometria oraz podstawowe parametry ( długość , szerokość ) .
Stare , niezbyt bezpieczne bandy , wymieniono na nowe , założono nowy drenaż , gdyż poprzedni praktycznie już nie działał .
W roku 2001 rozpoczął się remont trybun .
Zmodernizowane zostały całe trybuny , włącznie z trybuną krytą .
W lutym 2003 roku zamontowano cztery czterdziestometrowe słupy oświetleniowe .
Przed sezonem 2006 zostały ponadto zamontowane dmuchane bandy , które stały się warunkiem dopuszczenia stadionu do rozgrywania meczów Ekstraligi .
W 2013 r. dokonano modernizacji stadionu , w ramach której przebudowano boisko do piłki nożnej , tak by mogło ono spełniać wymogi Polskiego Związku Piłki Nożnej .
Pod murawą zainstalowano nowoczesny system nawadniania i podgrzewania murawy .
Powiększenie boiska oznaczało także , że trzeba było przebudować żużlowy tor okalający pole do gry .
Dojazd .
W okolice stadionu dojeżdżają autobusy komunikacji miejskiej o numerach : 11 , 12 , 13 , 14 .
