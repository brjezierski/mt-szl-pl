Stalowa Wola ( przystanek kolejowy )
Stalowa Wola – przystanek kolejowy w Stalowej Woli , w województwie podkarpackim , w Polsce .
Do niedawna zatrzymywały się tutaj pociągi TLK , dzisiaj już tylko pociągi Regio .
Na terenie przystanku znajduje się zamknięty wybudowany na początku lat 50. budynek stacyjny , będący w złym stanie , w którym do 2012 roku znajdowały się czynne kasy biletowe .
W roku 2017 przystanek obsługiwał 100–149 pasażerów na dobę .
