Stambuł ( prowincja )
Prowincja Stambuł ( tur .
: `` İstanbul '' ) – jednostka administracyjna w północno-zachodniej Turcji , położona nad Morzami Czarnym i Marmara .
Od wschodu graniczy z prowincją Kocaeli , a od zachodu z Tekirdağ .
Jest przedzielona na część europejską i azjatycką przez cieśninę Bosfor .
Według spisu powszechnego z 2000 roku ok. 3,8 mln osób mieszka w części azjatyckiej , a 9,4 mln - w europejskiej .
Stolicą prowincji jest Stambuł , który od 2004 roku ma z nią tożsame granice .
Prowincja jest najgęściej zaludnioną prowincją w kraju - w roku 2021 zamieszkiwało ją 15 840 900 ludzi , czyli 18,6 % ludności Turcji .
