Stange ( stacja kolejowa )
Stange – stacja kolejowa w Stange , w regionie Hedmark w Norwegii , jest oddalona od Oslo Sentralstasjon o 114,42 km .
Jest położona na wysokości 222,4 m n.p.m .
Trondheim
Ruch lokalny .
Leży na linii Dovrebanen .
Stacja obsługuje ruch lokalny do Oslo Sentralstasjon i Lillehammer ; pociągi na tej trasie jeżdżą co dwie godziny w obie strony .
Obsługa pasażerów .
