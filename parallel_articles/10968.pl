Stanisław Korfanty
Stanisław Andrzej Korfanty ( ur .
12 listopada 1957 w Piekarach Śląskich ) – polski samorządowiec , od 2002 do 2014 prezydent Piekar Śląskich .
Życiorys .
Ukończył studia na Politechnice Śląskiej w Gliwicach , uzyskując tytuł zawodowy magistra inżyniera w zakresie elektroniki , a także podyplomowe studia na Akademii Ekonomicznej w Katowicach .
Uzyskał licencje zarządcy nieruchomości i członka rad nadzorczych spółek Skarbu Państwa .
Był dyrektorem siemianowickiego Centrum Leczenia Oparzeń .
W wyborach samorządowych w 2002 kandydował na prezydenta miasta Piekary Śląskie .
W I turze wyborów otrzymał 6768 głosów ( 39,46 % ) , w II turze wyborów wynik 9773 głosy ( 60,32 % ) pozwolił mu pokonać jego kontrkandydata , dotychczasowego prezydenta Andrzeja Żydka .
W wyborach samorządowych w 2006 skutecznie ubiegał się o reelekcję , zdobywając 13 266 głosów ( 65,78 % ) i zwyciężając tym samym już w I turze wyborów .
Również w 2010 wygrał w pierwszej turze .
W 2014 nie uzyskał reelekcji , został natomiast wybrany na radnego rady miejskiej .
Mandat radnego utrzymał również w 2018 .
Jest żonaty , ma trzech synów .
Od urodzenia mieszka w dzielnicy Dąbrówka Wielka .
W 2016 otrzymał Złoty Krzyż Zasługi .
