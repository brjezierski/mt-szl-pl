Stanisław Pietkiewicz
Stanisław Pietkiewicz ( ur .
9 sierpnia 1894 w Cybulowie , zm .
23 kwietnia 1986 w Warszawie ) – polski geograf i kartograf .
Życiorys .
Odbył studia hydro-inżynieryjne w Instytucie Politechnicznym w Piotrogrodzie oraz studia geograficzne w Wolnej Wszechnicy Polskiej i Uniwersytecie Warszawskim .
Studiował także w uniwersytecie w Paryżu .
Zweryfikowany w stopniu porucznika ze starszeństwem z 1 czerwca 1919 w korpusie oficerów rezerwy geografów .
W 1934 pozostawał w ewidencji Powiatowej Komendy Uzupełnień Warszawa Miasto III .
W 1929 otrzymał tytuł doktora za pracę `` Pojezierze Suwalszczyzny Zachodniej .
Zarys morfologii lodowcowej . ''
W latach 1930–1939 pracownik Katedry Geografii , w 1950–1964 kierownik Katedry Kartografii Uniwersytetu Warszawskiego .
Od 1936 docent geografii fizycznej Uniwersytetu Warszawskiego ( praca `` O sposobach przedstawiania terenu na mapach '' ) .
Od 1954 profesor nadzwyczajny tej uczelni .
Równocześnie profesor Katedry Geografii Fizycznej Uniwersytetu Łódzkiego ( 1950–1961 ) oraz kierownik , a później profesor Pracowni Geografii Polskiej Akademii Nauk ( 1954–1962 ) .
W latach 1920–1922 brał czynny udział w wytyczaniu granicy państwowej Polski w Wielkopolsce , na Pomorzu i Mazurach , a w 1946 w Białostockiem .
Współpracownik Polsko-Radzieckiej Komisji Delimitacyjnej ( 1947–1950 ) .
Od 1947 członek korespondencyjny Towarzystwa Naukowego Warszawskiego .
W latach 1948–1949 wykładał geografię i geologię w Oficerskiej Szkole Służby Geograficznej w Warszawie .
Członek Polskiego Towarzystwa Geologicznego
W pracy naukowej zajmował się przede wszystkim kartografią , ale także mapoznawstwem oraz historią geografii .
Prowadził pionierskie w skali światowej badania elementów na dawnych mapach , opracował liczne mapy południowej Polski , współpracował nad `` Przeglądową mapą geologiczną Polski '' .
Zmarł w Warszawie .
Pochowany na cmentarzu Powązkowskim ( kwatera 256-5-15 ) .
