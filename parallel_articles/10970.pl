Stanisław Tym
Stanisław Aleksy Tym ( ur .
17 lipca 1937 w Małkini Górnej ) – polski aktor teatralny , telewizyjny i filmowy , satyryk , reżyser , scenarzysta i literat .
Życiorys .
W 1953 zdał maturę w CLXV Liceum Ogólnokształcącym w Warszawie .
Na przełomie lat 50. i 60 .
XX wieku studiował w Warszawie , m.in .
chemię na Politechnice Warszawskiej , przetwórstwo na SGGW , a także na Wydziale Aktorskim PWST , którego jednak nie ukończył , oblewając egzamin z dykcji .
W czasie studiów pisał teksty i występował w kabaretach studenckich .
Od 1956 pracował w klubach studenckich .
Był bramkarzem i szatniarzem w klubie „ Stodoła ” , gdzie następnie występował w kabarecie .
W 1964 zdał w Warszawie eksternistyczny egzamin aktorski .
W latach 1960–1972 był aktorem , autorem i reżyserem oraz dyrektorem warszawskiego Studenckiego Teatru Satyryków .
W latach 1966–1970 był współautorem tekstów i aktorem kabaretów `` Owca '' , `` Dudek '' , `` Lopek '' i `` Wagabunda '' .
Autor wielu sztuk teatralnych , skeczy ( jednym ze słynniejszych jest `` Ucz się Jasiu '' w wykonaniu Jana Kobuszewskiego , Wiesława Gołasa i Wiesława Michnikowskiego , prezentowany w kabarecie „ Dudek ” ) i słuchowisk radiowych , a także scenariuszy do filmów Stanisława Barei , w których również występował i był współreżyserem .
