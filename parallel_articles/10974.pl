Izba Gwiaździsta
Izba Gwiaździsta , Sąd Izby Gwiaździstej ( ang .
`` Star Chamber '' , `` Court of Star Chamber '' ) – angielski sąd o specjalnych uprawnieniach , który zaczął kształtować się w czasach panowania króla Edwarda II ( pierwsza wzmianka pochodzi z 1398 r. ) .
Wywodził się ze średniowiecznej Rady Królewskiej , późniejszej Tajnej Rady .
W 1487 r. został zreorganizowany przez króla Henryka VII i działał jako sąd w sprawach kryminalnych i politycznych .
Sądził oskarżonych o przestępstwa przeciwko monarsze i państwu , np .
zdrada stanu .
Miał zwalczać bezprawie w organach administracji państwowej , anarchię oraz karać buntowników .
Ponadto od tego czasu jego posiedzenia odbywały się w jednej z sal Pałacu Westminsterskiego w Londynie , która była ozdobiona gwiazdami ( stąd nazwa ) .
W niektórych procesach przed Izbą Gwiaździstą stosowane były tortury , których jednak nie dopuszcza system `` common law '' .
W takich sytuacjach powoływano się na procedurę inkwizycyjną .
W czasach monarchii absolutnej w Anglii sąd ten był narzędziem króla w walce z przeciwnikami politycznymi .
Za panowania Karola I Stuarta służył do walki z antyrojalistyczną purytańską opozycją .
Sąd Izby Gwiaździstej został zniesiony przez Długi Parlament ustawą z 5 lipca 1641 r .
