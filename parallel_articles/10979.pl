Stare Kotkowice
Stare Kotkowice ( dodatkowa nazwa w j. niem .
Alt Kuttendorf ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Kotliny Raciborskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 409 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 11 km od granicy z Czechami , w zachodniej części Kotliny Raciborskiej .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
