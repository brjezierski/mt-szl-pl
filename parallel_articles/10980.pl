Malanowo Stare
Malanowo Stare ( do 31 grudnia 2002 `` Stare Malanowo '' ) – wieś w Polsce położona w województwie mazowieckim , w powiecie sierpeckim , w gminie Mochowo .
Leży nad Skrwą .
W latach 1975–1998 miejscowość administracyjnie należała do województwa płockiego .
1 stycznia 2003 nastąpiła zmiana nazwy wsi ze `` Stare Malanowo '' na `` Malanowo Stare '' .
