Stare Polaszki
Stare Polaszki ( kaszb .
`` Stôré Pòlaszczi '' , niem .
`` Alt Paleschken '' ) – wieś pogranicza kaszubsko-kociewskiego w Polsce położona w województwie pomorskim , w powiecie kościerskim , w gminie Stara Kiszewa nad dopływem Wierzycy Kaczynką .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
