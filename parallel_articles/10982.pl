Stare Radzikowo
Stare Radzikowo – wieś w Polsce położona w województwie mazowieckim , w powiecie płońskim , w gminie Czerwińsk nad Wisłą .
W latach 1975–1998 miejscowość administracyjnie należała do województwa płockiego .
Historia .
Pierwsza wzmianka o Radzikowie pochodzi z 1221 r. Na terenie wsi znajdują się dwa grodziska .
Starsze tzw .
`` Gaik '' szacowane na VI-VII wiek miało charakter miejsca kultowego , a młodsze tzw .
`` Okop '' na XII wiek ( ok. 750 m na północny zachód od kościoła , przecięte szosą Czerwińsk-Płońsk ) .
