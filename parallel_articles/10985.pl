Stare Serby
Stare Serby ( niem .
`` Alt Lerchenberg '' , dawniej `` Lerchenberg '' ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie głogowskim , w gminie Głogów .
Podział administracyjny .
W latach 1975–1998 miejscowość położona była w województwie legnickim .
15 marca 1984 część wsi ( 69 ha ) włączono do Głogowa .
