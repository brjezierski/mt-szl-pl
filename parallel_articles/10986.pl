Stare Tarnowice
Stare Tarnowice ( niem .
`` Alt Tarnowitz '' ) – część miasta i dzielnica Tarnowskich Gór , od której pochodzi nazwa miasta .
Powstała w 1316 .
Pierwszym właścicielem był prawdopodobnie rycerz Adam de Tarnowice , a następnie jego syn ( lub brat ) Święcisław , później Włodkowie , Wrochemowie i inni .
Historia .
Najdawniejsza wzmianka o Tarnowicach ( jednak o kwestionowanej datacji ) pochodzi z 1251 roku .
Późniejsze wzmianki pochodzą z początku XIV wieku , kiedy właścicielem osady był Adam de Tarnowice , występujący jako świadek w dokumentach między 1310 a 1351 rokiem .
Później wsią władał Święcisław , członek dworu księcia oleśnickiego Konrada II , wyznaczony w 1369 roku jako rozjemca przy sporze o podział księstwa bytomskiego .
Ostatnimi znanymi rycerzami z Tarnowic są : rycerz Sambor , wzmiankowany w 1411 roku , oraz Mikołaj i Teodor Włodkowie herbu Sulima , poświadczeni w 1415 roku .
Siedzibą rycerską w XIV i XV wieku była przypuszczalnie wieża obronna wzniesiona na kopcu , którego pozostałości widoczne są za kościołem św .
Marcina .
