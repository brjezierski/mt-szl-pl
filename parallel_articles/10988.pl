Stare Okniny
Stare Okniny – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Przez wieś przebiega droga powiatowa Zabłocie – Gostchorz .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Andrzeja z Awelinu w Radomyślu .
W miejscowości działa założona w 1965 roku jednostka ochotniczej straży pożarnej .
Jednostka posiada dwa samochody gaśnicze : średni GBA 2,5/16 Star 266 oraz ciężki GCBA 5/24/2,5 Jelcz 010
