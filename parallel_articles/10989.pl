Stare Opole
Stare Opole – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 8 km na zachód od Siedlec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W Starym Opolu funkcjonuje rzymskokatolicka parafia MB Nieustającej Pomocy .
Przez Stare Opole przebiega trasa międzynarodowa E30 Cork–Omsk oraz linia kolejowa Warszawa–Terespol .
Przystanek kolejowy o nazwie Sabinka .
Pierwsza wzmianka pochodzi z 1476 .
Dnia 30 stycznia 2021 tuż obok Centrum Logistycznego Produktów Świeżych Topaz w Starym Opolu został otworzony pierwszy Topaz typu `` Cash and carry '' .
