Stare Orzechowo
Stare Orzechowo – wieś w Polsce położona w województwie mazowieckim , w powiecie nowodworskim , w gminie Pomiechówek .
Leży nad Narwią .
Historia .
W II wieku naszej ery istniała tu osada , której mieszkańcy trudnili się rybołówstwem , rolnictwem i garncarstwem .
Przechodził przez te obszary tzw .
Szlak Waregów .
Pierwsza wzmianka o Orzechowie pochodzi z 1065 roku ( Falsyfikat Mogileński ) .
W 1150 roku Piotr Piotrowicz przekazuje Orzechowo klasztorowi Benedyktynów w Sieciechowie .
