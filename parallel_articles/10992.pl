Położenie .
Według danych z 1 stycznia 2010 r. powierzchnia gminy wynosiła 318,88 km² .
Gmina stanowi 21,0 % powierzchni powiatu .
Sąsiednie gminy :
Do 31 XII 1998 r. wchodziła w skład województwa szczecińskiego .
Przyroda i turystyka .
Gmina leży na równinach : Pyrzyckiej i Nowogardzkiej .
Po włączeniu wsi Burzykowo , Słotnica oraz lotniska w Kluczewie w granice Stargardu , obszar gminy został podzielony na dwie części .
W zachodniej części znajduje się większość jeziora Miedwie .
Przez wschodnią część przepływają rzeki Ina oraz jej dopływy : Krąpiel i Mała Ina .
W okolicach Stargardu znajduje się rezerwat przyrody Ozy Kiczarowskie .
Tereny leśne zajmują 12 % , a użytki rolne 72 % .
