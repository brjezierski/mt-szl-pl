Transport .
Przez gminę prowadzą drogi krajowe nr 10 łącząca Stargard przez Kobylankę ( 12 km ) ze Szczecinem ( 24 km do autostrady A6 ) i z Suchaniem ( 20 km , kier .
Bydgoszcz ) oraz nr 20 do Chociwla ( 26 km , kier .
Gdynia ) .
Przez gminę prowadzą także drogi wojewódzkie nr 144 przez Warnice ( 11 km ) do Pyrzyc ( 28 km ) i przez wieś Łęczycę ( 13 km ) i Darż ( 18 km ) do Maszewa ( 20 km ) , nr 142 łącząca wieś Sowno z Łęczycą ( 11 km ) i Rzęśnicą ( 10 km ) oraz nr 141 z Sowna do wsi Darż ( 13 km ) .
Siedziba gminy , Stargard uzyskał połączenie kolejowe w 1846 r. , po wybudowaniu torów ze Szczecina , 2 lata później ukończona była już cała linia do Poznania .
W 1859 r. powstał odcinek do Koszalina , 10 lat później przedłużony do Gdańska .
W 1882 r. otwarto linię do Pyrzyc , a w 1895 odcinek do Kalisza Pomorskiego ( ostatni na linii do Piły ) .
Także w 1895 r. zbudowano linię Stargardzkiej Kolei Wąskotorowej ( rozstaw szyn 1000 mm ) do Dobrej Nowogardzkiej .
Po ostatnim kwartale XX wieku dwie najważniejsze linie zostały zelektryfikowane : ze Szczecina do Poznania do 1978 r. , a do Gdańska do 1989 r. Po 2000 r. niezelektryfikowane linie zostały zamknięte : w 2000 r. do Piły ( później jeszcze kilkakrotnie ruch pasażerski na odcinku do Kalisza Pomorskiego był przywracany , obecnie reaktywowana ) , w 2001 r. linię wąskotorową do Dobrej , a w 2003 r. odcinek do Pyrzyc .
Obecnie w gminie czynnych jest 5 stacji : Grzędzice Stargardzkie , Ulikowo , Witkowo Pyrzyckie , Pęzino i Barzkowice .
W gminie czynny jest 1 urząd pocztowy : Pęzino ( nr 73-131 ) .
Gminę obsługują także urzędy pocztowe w Stargardzie .
Oświata .
Na terenie gminy funkcjonuje 5 szkół podstawowych w : Grzędzicach , Małkocinie , Pęzinie , Strachocinie i Sownie .
Administracja .
W 2016 r. wykonane wydatki budżetu gminy wiejskiej Stargard wynosiły 40,8 mln zł , a dochody budżetu 44,7 mln zł .
Zobowiązania samorządu ( dług publiczny ) według stanu na koniec 2016 r. wynosiły 11,9 mln zł , co stanowiło 26,6 % poziomu dochodów .
