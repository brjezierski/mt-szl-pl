Stara Rudnica
Stara Rudnica ( niem .
`` Alt Rūdnitz '' ) – wieś w Polsce , położona w województwie zachodniopomorskim , w powiecie gryfińskim , w gminie Cedynia .
W latach 1975–1998 miejscowość należała administracyjnie do województwa szczecińskiego .
Według danych z 1 stycznia 2011 roku wieś liczyła 148 mieszkańców .
Nazwa .
12 listopada 1946 r. nadano miejscowości polską nazwę Stara Rudnica .
Historia .
Wieś o starej metryce , dawne osiedle pomorskich rybaków i rolników .
W 1945 miała tu miejsce koncentracja 1 Armii Wojska Polskiego i 61 Armii Radzieckiej dowodzonej przez gen. Biełowa , a następnie obie armie rozpoczęły szturm na Berlin .
Zabudowa wsi w tym czasie została mocno zniszczona .
We wsi kościół szachulcowy z XV w. , przebudowany w 1839 z wieżą , liczne budynki ryglowe z XVIII-XX w .
