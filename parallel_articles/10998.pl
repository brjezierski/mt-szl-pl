Stara Sucha ( powiat węgrowski )
Stara Sucha – wieś w Polsce położona w województwie mazowieckim , w powiecie węgrowskim , w gminie Grębków .
Przez miejscowość przepływa Kostrzyń , rzeka dorzecza Bugu .
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Królowej Korony Polskiej w Kopciach .
W latach 1975–1998 miejscowość należała administracyjnie do województwa siedleckiego .
