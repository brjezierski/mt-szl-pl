Olszewnica Stara
Olszewnica Stara ( do 14 lutego 2002 `` Stara Olszewnica '' ) – wieś w Polsce położona w województwie mazowieckim , w powiecie legionowskim , w gminie Wieliszew .
Graniczy ze wsiami : Chotomów , Olszewnica Nowa , Kałuszyn , Skrzeszew .
Jest miejscowością średniej wielkości .
Na koniec 2011 roku posiadała 739 mieszkańców .
Są to rolnicy , ogrodnicy , rzemieślnicy , prywatni przedsiębiorcy , pracownicy umysłowi itp .
Wieś zajmuje powierzchnię 654 ha .
Wieś jest zelektryfikowana , ma instalację gazu ziemnego i wodociąg .
