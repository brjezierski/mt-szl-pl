– miasto we wschodniej Hiszpanii , w regionie Walencja , stolica prowincji Alicante .
Kąpielisko na Costa Blanca , położone u stóp skalistego wzniesienia , na którym wznosi się Castillo de Santa Bárbara .
Historia .
