Stara Zagość
Stara Zagość – wieś w Polsce położona w województwie świętokrzyskim , w powiecie pińczowskim , w gminie Pińczów .
Leży nad rzeką Nidą , ok. 9 km od Pińczowa .
Historia Zagości .
Zagość jako osada otwarta powstała pomiędzy przełomem VIII i IX w. a X w. W 1784 r. wieś administracyjnie należała do powiatu wiślickiego w województwie sandomierskim .
W tym czasie była własnością starosty zagojskiego Feliksa Łubieńskiego .
W 1827 r. nadal należała do powiatu stopnickiego w województwie krakowskim , ale wchodziła już w skład własności „ rządowej ” , czyli należała do skarbu państwa i liczyła 64 domy i 369 mieszkańców .
Do 1954 roku siedziba gminy Zagość .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
