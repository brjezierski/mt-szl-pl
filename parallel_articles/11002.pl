W latach 1939–1940 w Starobielsku na obszarze byłego klasztoru mieścił się obóz jeniecki dla Polaków , oficerów ( także podchorążych ) służby stałej i rezerwy Wojska Polskiego , wziętych do niewoli przez Związek Radziecki po zajęciu wschodnich obszarów Polski po 17 września 1939 roku .
W kwietniu i maju 1940 roku byli oni przewożeni sukcesywnie do Charkowa i rozstrzeliwani przez funkcjonariuszy Obwodowego Zarządu NKWD w Charkowie oraz pracowników NKWD przybyłych z Moskwy na mocy decyzji Biura Politycznego KC WKP ( b ) z 5 marca 1940 roku ( część zbrodni katyńskiej ) .
Zamordowani jeńcy są pochowani na Cmentarzu Ofiar Totalitaryzmu w Charkowie w Piatichatkach .
48 Polaków zmarło w obozie starobielskim i zostało pochowanych na starym cmentarzu miejskim , w 1995 roku przeniesiono ich ciała na cmentarz czmirowski .
