Należy odróżniać też pojęcia `` język staropolski '' oraz `` literatura staropolska '' .
Okres staropolski literatury obejmuje dłuższy czas – od X w. do połowy XVIII w. ( czyli nie tylko literaturę średniowieczną , ale także renesansową i barokową ) .
