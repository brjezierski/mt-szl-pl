Starowola ( Białoruś )
Starowola ( ; ) – agromiasteczko na Białorusi , w obwodzie brzeskim , w rejonie prużańskim , w sielsowiecie Wielkie Sioło .
Od skrzyżowania z drogą republikańską zaczyna tu bieg droga republikańska .
Urodził tu się ks .
Szymon Starowolski oraz bp Kazimierz Wielikosielec OP .
Cykl obrazów przedstawiających Starowolę ( głównie dwór ) stworzył Stanisław Żukowski .
W dwudziestoleciu międzywojennym leżała w Polsce , w województwie poleskim , w powiecie prużańskim .
