Starocin
Starocin – wieś w Polsce położona w województwie pomorskim , w powiecie nowodworskim , w gminie Nowy Dwór Gdański na obszarze Żuław Wiślanych .
Wieś jest siedzibą sołectwa `` Starocin '' w którego skład wchodzi również miejscowość Piotrowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
