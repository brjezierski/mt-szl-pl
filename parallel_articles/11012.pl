Stary Jarużyn
Stary Jarużyn – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie nakielskim , w gminie Szubin .
Podział administracyjny .
W latach 1954–1957 wieś należała i była siedzibą władz gromady Jarużyn , po jej zniesieniu w gromadzie Samoklęski Małe .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 249 mieszkańców .
Jest 19. co do wielkości miejscowością gminy Szubin .
