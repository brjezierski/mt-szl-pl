Stary Las ( województwo wielkopolskie )
Stary Las – osada leśna w Polsce położony w województwie wielkopolskim , w powiecie krotoszyńskim , w gminie Krotoszyn .
Miejscowość wchodzi w skład sołectwa Roszki .
W latach 1975–1998 miejscowość położona była w województwie kaliskim .
