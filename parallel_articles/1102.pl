Oleszewicze
Oleszewicze ( , `` Alieszawiczy '' ; , `` Olieszewiczi '' ) – wieś na Białorusi , w rejonie mostowskim obwodu grodzieńskiego , około 12 km na północny wschód od Mostów .
Historia .
Pod koniec XVIII wieku Jan Niezabytowski herbu Lubicz kupił majątek Oleszewicze od rodziny Baranowiczów .
Kolejnymi właścicielami tych dóbr byli potomkowie Jana : syn Krzysztof , wnuk Konstanty i prawnuk Karol Niezabytowski .
Po 1920 roku w wyniku działów rodzinnych Oleszewicze zostały własnością najmłodszej córki Karola , Ireny późniejszej Ronikier ( 1907–1984 ) , która była ostatnią właścicielką Oleszewicz w 1939 roku .
Po III rozbiorze Polski w 1795 roku Oleszewicze , wcześniej należące do województwa trockiego Rzeczypospolitej , znalazły się na terenie guberni grodzieńskiej Imperium Rosyjskiego .
Po ustabilizowaniu się granicy polsko-radzieckiej w 1921 roku Oleszewicze wróciły do Polski , znalazły się w gminie Kamionka w powiecie grodzieńskim województwa białostockiego .
W 1929 roku gmina ta weszła w skład nowo otworzonego powiatu szczuczyńskiego w województwie nowogródzkim .
