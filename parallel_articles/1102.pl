Od 1945 roku – w ZSRR , od 1991 roku – na terenie Republiki Białorusi .
Do 2013 roku Oleszewicze należały do sielsowietu Mikielewszczyzna .
W 2009 roku wieś liczyła 34 mieszkańców .
