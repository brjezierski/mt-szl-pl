Stary Targ
Stary Targ ( niem .
`` Altmark '' ) – wieś w Polsce , w województwie pomorskim , w powiecie sztumskim , w gminie Stary Targ , której jest siedzibą .
Miejscowość jest także siedzibą sołectwa Stary Targ , w którego skład wchodzą również Stary Dwór i Waplewo-Osiedle .
Stary Targ jest położony przy drodze wojewódzkiej nr 517 .
W miejscowości znajdują się m.in .
