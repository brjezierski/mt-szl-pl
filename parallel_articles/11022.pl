Stary Toruń
Stary Toruń – wieś ( dawniej miasto ) w Polsce położona w województwie kujawsko-pomorskim , w powiecie toruńskim , w gminie Zławieś Wielka .
Historia .
Nad Wisłą na południe od współczesnej wsi w 1233 r. lokowany był Toruń , skąd po około 3 latach dokonano translokacji ( przeniesienia ) miasta na obecne miejsce .
Według źródeł istniała tutaj warownia krzyżacka oraz osada z kościołem .
W 1242 r. gród oparł się najazdowi księcia gdańskiego Świętopełka .
W latach 1244-1247 , w czasie wojny krzyżacko-pomorskiej , stanowił rezydencję mistrza krajowego Poppo von Osternohe .
W czasie wojen polsko-krzyżackich w 1. poł .
XV w. gród został zniszczony .
