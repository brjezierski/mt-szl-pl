Stary Orzechów
Stary Orzechów – wieś w Polsce położona w województwie lubelskim , w powiecie parczewskim , w gminie Sosnowica .
W pobliżu znajduje się łowieckie jezioro Skomelno .
Wieś stanowi sołectwo gminy Sosnowica .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
Wierni Kościoła rzymskokatolickiego należą do parafii Trójcy Świętej w Sosnowicy .
Historia .
Orzechów Stary w dokumentach z roku 1252 opisano jako rzekomą własność klasztoru sieciechowskiego .
W roku 1564 występuje jako „ Orzechow uterque '' wieś szlachecka .
