Starzno
Starzno ( niem .
`` Starsen '' , kaszb .
`` Starzno '' ) – wieś w Polsce położona w województwie pomorskim , w powiecie człuchowskim , w gminie Koczała nad jeziorem Starzeńskim .
Wieś królewska starostwa człuchowskiego w województwie pomorskim w drugiej połowie XVI wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
Historia .
Starzno istniało prawdopodobnie jeszcze przed 1310 roku .
Brakuje jednak dokumentów umożliwiających odtworzenie dziejów miejscowości w tamtych czasach .
