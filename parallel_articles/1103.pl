Alkmaar ( Surinam )
Alkmaar – miasto w dystrykcie Commewijne , w Surinamie .
Według danych na rok 2012 miasto zamieszkiwało 5561 osób , a gęstość zaludnienia wyniosła 68,65 os./km2 .
