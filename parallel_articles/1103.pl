Klimat .
Klimat jest tropikalny .
Średnia temperatura powietrza wynosi 23 °C .
Najcieplejszym miesiącem jest październik ( 24 °C ) , a najzimniejszym miesiącem jest czerwiec ( 22 °C ) .
Średnie opady wynoszą 2503 milimetrów rocznie .
Najbardziej wilgotnym miesiącem jest maj ( 448 milimetrów deszczu ) , a najbardziej suchym miesiącem jest wrzesień ( 69 milimetrów deszczu ) .
