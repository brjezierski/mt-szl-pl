Język staroturecki
Język staroturecki , język staroturkijski ( ) – nazwa używana w odniesieniu do najstarszego zaświadczonego w piśmie języka turkijskiego .
Zaliczał się on do południowo-wschodniej gałęzi języków turkijskich , która dała początek ujgurskiemu oraz czagatajskiemu .
Istnieje wiele zapisów w starotureckim , przy czym w większości z nich zastosowano zapis w piśmie orchońskim .
