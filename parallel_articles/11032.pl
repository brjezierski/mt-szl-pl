Staszów ( gmina )
Staszów – gmina miejsko-wiejska w województwie świętokrzyskim , w powiecie staszowskim .
W latach 1975–1998 gmina położona była w województwie tarnobrzeskim .
Siedziba gminy to Staszów .
Według danych z 30 czerwca 2009 gminę zamieszkiwało 26 205 osób .
Natomiast według danych z 31 grudnia 2019 roku gminę zamieszkiwało 25 599 osób .
