Stavanger
Stavanger ( wym .
[ ] ) – norweskie miasto portowe i gmina leżąca w okręgu Rogaland , na południowo-zachodnim wybrzeżu Norwegii .
Historia Rady Okręgu sięga roku 1837 , ale dopiero w roku 1976 odbyły się lokalne wybory do rządu okręgowego .
W Stavangerze , w najstarszej części miasta ( `` Gamle Stavanger '' ) znajduje się Muzeum Konserw ( `` Norsk Hermetikkmuseum '' ) .
Niegdyś funkcjonowało w mieście kilkadziesiąt zakładów produkujących konserwy i przetwarzających ryby .
Miasto znane jest również z połowów szprotek .
Do najcenniejszych zabytków należą m.in .
: katedra romańska z przełomu XI i XII w. , dwór Ledaal z XVIII w. oraz Pałac Biskupi , zawierający bogate zbiory archeologiczne , zwłaszcza z epoki brązu .
Historia .
Pierwsze ślady osadnictwa w regionie Stavanger pochodzą z momentu cofnięcia się lądolodu po ostatniej epoce lodowcowej ( ok. 10 000 lat temu ) .
Około 1100–1300 r. Stavanger stał się centrum administracji kościelnej i ważnym miastem handlowym na południowo-zachodnim wybrzeżu Norwegii .
