Staw ( jezioro w województwie warmińsko-mazurskim )
Staw ( lub Szlagowski Staw ) – jezioro w Polsce , w województwie warmińsko-mazurskim , w powiecie olsztyńskim , w gminie Olsztynek .
Położenie i charakterystyka .
Jezioro leży na Pojezierzu Olsztyńskim , natomiast w regionalizacji przyrodniczo-leśnej – w mezoregionie Puszcz Mazurskich , w dorzeczu Marózka–Łyna–Pregoła .
Znajduje się około 6 km w kierunku wschodnim od Olsztynka , nad jego północnymi brzegami leży osada Kołatek .
Od strony południowej wpada ciek wodny łączący akwen z Niskim Jeziorem , wypływa natomiast na północy , kierując wody do Jeziora Plusznego Małego i Wielkiego .
Zbiornik wodny leży w otoczeniu lasów – na wschodzie , na południu podmokłe łąki i mokradła na płaskich brzegach .
Dno i ławica przybrzeżna muliste .
Linia brzegowa niezbyt rozwinięta .
