Stawiski ( folwark )
Stawiski – dobra i nieistniejący już folwark w Polsce położony w województwie podlaskim , w powiecie kolneńskim , w gminie Stawiski .
Obecnie część miasta Stawiski .
Historia .
Stawiski w 1585 roku były własnością Andrzej Węgierskiego .
W drugiej połowie XVII wieku , były własnością Fortunata Zamoyskiego , herbu Grzymała , Łowczego Ziemi Łomżyńskiej i Starosty Wysokodworskiego .
W XVIII w. należały do Kramkowskich a następnie do Kisielnickich .
W skład dóbr wchodziły w 1886 folwark Stawiski , miasto Stawiski , wieś Chmielewo i wieś Budy Stawiskie .
W skład folwarku wchodziła cześć rezydencjonalna z murowanym dworem i parkiem oraz część gospodarcza .
