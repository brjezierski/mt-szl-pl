Kraj Stawropolski
Kraj Stawropolski ( ) – jednostka administracyjna Rosji w Północnokaukaskim Okręgu Federalnym ze stolicą w Stawropolu .
Geografia .
Kraj Stawropolski położony jest na Kaukazie , na południu europejskiej części Federacji Rosyjskiej .
Od północy graniczy z obwodem rostowskim , Kałmucją , od wschodu z Dagestanem , od południa z Czeczenią , Osetią Północną , Kabardo-Bałkarią oraz Karaczajo-Czerkiesją , a od zachodu z Krajem Krasnodarskim .
Przez Kraj Stawropolski przepływa rzeka Kubań .
Ludność .
W 1989 roku Kraj Stawropolski zamieszkiwało 2 410 379 osób , w 2002 roku liczba ta wzrosła do 2 735 139 osób , a według spisu z 2010 roku liczba ludności wynosiła 2 786 281 osób .
