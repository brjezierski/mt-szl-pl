Polonica .
Był to jeden z regionów zesłań Polaków w carskiej Rosji .
Pamiątką z tego okresu jest zabytkowy kościół Przemienienia Pańskiego w Piatigorsku .
Tablice rejestracyjne .
Tablice pojazdów zarejestrowanych w Kraju Stawropolskim mają oznaczenie 26 w prawym górnym rogu nad flagą Rosji i literami RUS .
