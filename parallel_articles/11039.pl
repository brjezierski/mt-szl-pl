Stawyszcze
Stawyszcze ( ; pol .
Stawiszcze ) − osiedle typu miejskiego na Ukrainie , w obwodzie kijowskim , w rejonie białocerkiewskim .
Do 2020 roku siedziba władz rejonu stawyszczenskiego .
Leży na Wyżynie Naddnieprzańskiej , nad rzeką Tykicz Gniły .
W 2018 roku liczyło ok. 6,5 tys .
mieszkańców .
Historia .
Pierwsza wzmianka o miejscowości pochodzi z roku 1622 .
W 1664 i 1665 r. w miejscowości trwały walki pomiędzy wojskami koronnymi a kozakami , zakończona pacyfikacją miasteczka .
Siedziba dawnej w powiecie taraszczańskim Gubernia kijowska .
W 1959 liczyło 4626 mieszkańców .
W 1989 liczyło 8067 mieszkańców .
W 2013 liczyło 6928 mieszkańców .
Urodzeni .
W miejscowości tej urodził się Edmund Berezowski .
