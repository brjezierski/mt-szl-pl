Stańkowo
Stańkowo ( ) – wieś w Polsce położona w województwie pomorskim , w powiecie kwidzyńskim , w gminie Prabuty przy drodze wojewódzkiej nr 521 .
Na wschód od miejscowości znajduje się jezioro Burgale .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
Historia .
Pierwsze zapisy o miejscowości Stańkowo pochodzą z 1347 , kiedy to biskup pomezański Mikołaj I odnowił przywilej lokacyjny wsi .
W 1543 roku w Stankowie znajdował się kościół , żyło tu 20 chłopskich rodzin , które posiadały po trzy włóki ziemi .
W 1740 właścicielem Stańkowa został Karl Gustaw von Bardeleben , który założył park .
W 1789 Stankowo było wsią szlachecką z folwarkiem i kościołem .
