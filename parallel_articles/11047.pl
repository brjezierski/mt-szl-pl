Steenbergen
Steenbergen – gmina samorządowa i miasto w południowej Holandii , w prowincji Brabancja Północna .
Powszechnie uznaje się , że Steenbergen uzyskał prawa miejskie w roku 1272 .
W mieście znajduje się wiele zabytków z okresu od XVI w. do XIX w. Znajduje się tam także 3. co do wielkości kościół w Brabancji Północnej , a 15. w całej Holandii – Sint-Gummaruskerk , którego budowę zakończono w 1903 , a odrestaurowano w 1944 roku .
Kościół mierzy 83 m i jest typową neogotycką budowlą .
Architektami byli Jos Cuypers i Jan Stuyt .
W mieście znajduje się także niewielki port rzeczny .
W samym Steenbergen mieszkało w 2007 roku 12 440 osób , ponadto w Dinteloord 5680 , Nieuw-Vossemeer 2400 , Kruisland 2340 i w De Heen 580 .
Razem w całej gminie 23 440 .
