Stefan Wyszyński
( ur .
3 sierpnia 1901 w Zuzeli , zm .
28 maja 1981 w Warszawie ) – polski duchowny rzymskokatolicki , biskup diecezjalny lubelski w latach 1946–1948 , arcybiskup metropolita gnieźnieński i warszawski oraz prymas Polski w latach 1948–1981 , kardynał prezbiter od 1953 , zwany `` Prymasem Tysiąclecia '' , określany niekiedy jako mąż stanu , postrzegany jako obrońca praw człowieka , narodu i Kościoła , doktor prawa kanonicznego , kaznodzieja , publicysta , kapelan duszpasterstwa Wojska Polskiego .
Pośmiertnie odznaczony Orderem Orła Białego .
Błogosławiony Kościoła katolickiego .
Twórca akcji duszpasterskich : Jasnogórskich Ślubów Narodu Polskiego czy Wielkiej Nowenny Tysiąclecia , inwigilowany przez służby bezpieczeństwa Polski Ludowej oraz umieszczony w miejscu odosobnienia przez władze komunistyczne PRL .
Życiorys .
Dzieciństwo i młodość .
Urodził się 3 sierpnia 1901 o godzinie 3:00 w Zuzeli nad Bugiem na ziemi nurskiej ( pogranicze Mazowsza i Podlasia ) jako drugie dziecko wielodzietnej rodziny rolników : Stanisława ( organisty miejscowego kościoła ) i Julianny z d. Karp .
Tego samego dnia został ochrzczony w parafii Przemienienia Pańskiego w Zuzeli z rąk proboszcza ks .
Antoniego Lipowskiego .
W religijnej atmosferze domu rodzinnego uformował i pogłębił swoją wiarę , szczególnie kultu maryjnego , co było powodem wyboru przyszłej drogi życia kapłańskiego .
