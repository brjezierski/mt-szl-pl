W kościele św .
Bartłomieja w Andrzejewie przystąpił w maju 1911 do pierwszej komunii świętej .
W latach 1912–1915 był uczniem gimnazjum im .
Wojciecha Górskiego w Warszawie .
W 1913 przyjął sakrament bierzmowania w parafii Wniebowzięcia Najświętszej Maryi Panny w Andrzejewie z rąk bp .
Antoniego Juliana Nowowiejskiego .
Z powodu wojny w latach 1915–1917 uczęszczał do Prywatnej Siedmioklasowej Szkoły Handlowej Męskiej w Łomży .
W latach 1917–1920 uczył się w liceum włocławskim im .
Piusa X ( Niższe Seminarium Duchowne ) .
W latach 1920–1924 był klerykiem Wyższego Seminarium Duchownego we Włocławku .
Święcenia subdiakonatu przyjął 15 marca 1924 z rąk bp .
Stanisława Zdzitowieckiego , a święcenia diakonatu 5 kwietnia tegoż roku .
Prezbiter .
Święcenia kapłańskie , które zostały przełożone z 29 czerwca z powodu jego choroby płuc , przyjął 3 sierpnia 1924 ( w dniu swoich 23. urodzin ) w kaplicy Matki Bożej we włocławskiej bazylice katedralnej z rąk bp .
Wojciecha Owczarka .
Mszę prymicyjną odprawił 5 sierpnia w kaplicy Matki Boskiej Częstochowskiej na Jasnej Górze .
Następnie po odbytej kuracji , w październiku 1924 został wikariuszem we włocławskiej parafii katedralnej oraz m.in .
redaktorem dziennika diecezjalnego „ Słowo Kujawskie ” ( 1924–1925 ) .
W latach 1925–1929 był studentem Wydziału Prawa Kanonicznego oraz Wydziału Prawa i Nauk Ekonomiczno-Społecznych Katolickiego Uniwersytetu Lubelskiego , pod kierunkiem ks .
prof. Antoniego Szymańskiego , który ukończył 22 czerwca 1929 doktoratem z prawa kanonicznego pisząc pracę na temat `` Prawa rodziny , Kościoła i państwa do szkoły '' .
W okresie studiów działał społecznie , m.in .
w Stowarzyszeniu Katolickiej Młodzieży Akademickiej „ Odrodzenie ” oraz w „ Bratniej Pomocy ” .
Po ukończeniu studiów w okresie 1929–1930 odbył podróże studyjne w celu obserwacji rozwoju katolickiej myśli społecznej po niektórych krajach europejskich : Austrii , Włoszech , Francji , Belgii , Holandii i Niemczech .
Pomimo propozycji pracy na KUL i pisania habilitacji — ostatecznie odrzucił możliwość wykładów na katolickiej uczelni .
