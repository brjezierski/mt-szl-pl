Jego szkolny kolega z ławki w Zuzeli Franciszek Jastrzębski tak go wspomina :
Na naukę nie lubił poświęcać zbyt wiele czasu , a zwłaszcza na matematykę , której – jak sam przyznawał – najbardziej nie lubił .
Wolał się bawić w domu z siostrami .
Podczas zabawy zdarzyło się , że tak bardzo się na nie zdenerwował , że rozpruł ich szmaciane lalki i spalił je w piecu .
Gdy ojciec zabierał się do wymierzenia mu kary , schował się pod pianino .
Siostry solidarnie stanęły wtedy w jego obronie , mówiąc : „ On się poprawi , nawróci ... ” „ Jak widzicie , nawróciłem się ! ” – żartował później w jednym z kazań .
W 1910 rodzina przeniosła się do Andrzejewa , gdzie uczęszczał do trzeciej klasy szkoły podstawowej z rosyjskim językiem nauczania ( Polska była wówczas pod zaborami ) .
Tutaj popadł w konflikt z nauczycielem , za nieposłuszeństwo jego metodzie wychowawczej , co było powodem wyrzucenia ze szkoły .
Umierająca matka w 1910 dała mu do zrozumienia , że jej wolą i matczyną intuicją jest by został kapłanem .
