Stefania Turkewycz-Łukijanowycz
Stefania Iwaniwna Turkewycz , Stefanija Turkewycz-Łukijanowycz ( ukr .
: Стефанія Іванівна Туркевич-Лукіянович ; ur .
25 kwietnia 1898 w Brodach , zm .
8 kwietnia 1977 w Cambridge ) – ukraińska kompozytorka , pianistka , muzykolog .
Biografia .
Dzieciństwo .
Urodziła się jako pierwsze dziecko w rodzinie księdza , chórmistrza , katechety i krytyka muzycznego Iwana Turkewycza ( 1872–1936 ) i jego żony Sofiji zd .
Kormosziw .
Miała młodszą siostrę Irenę i młodszego brata Lwa .
Jej dziadek Lew Turkewycz , podobnie jak ojciec , był greckokatolickim duchownym , natomiast matka była pianistką i studiowała u Karola Mikuliego i Williama Kurza .
Cała rodzina była muzykalna i wszyscy grali na różnych instrumentach .
Bracia i siostry na wiolonczeli , skrzypcach , Stefania na fortepianie , harfie i harmonium .
Studia .
Pierwsze lekcje muzyki otrzymała od matki .
