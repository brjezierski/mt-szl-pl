Stemphylium vesicarium
Stemphylium vesicarium ( Wallr . )
E.G .
Simmons – gatunek grzybów z rodziny `` Pleosporaceae '' .
Grzyb mikroskopijny rozprzestrzeniony na całym świecie na roślinach z rodziny różowatych ( `` Rosaceae '' ) .
Sprawca brązowej plamistości gruszy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pleosporaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten w 1833 r. opisał Carl Friedrich Wilhelm Wallroth nadając mu nazwę `` Helminthosporium vesicarium '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu E.G .
Simmons w 1969 r .
