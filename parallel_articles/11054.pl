Morfologia .
`` Stemphylium vesicarium '' jest grzybem saprotroficznym i pasożytniczym atakującym wiele gatunków roślin .
Wśród roślin uprawnych oprócz gruszy poraża czosnek , cebulę , szparagi , lucernę , soję , mango , astra , wśród dziko rosnących liczne gatunki z rodziny wiechlinowatych ( `` Poaceae '' ) i bobowatych ( `` Fabaceae '' ) .
