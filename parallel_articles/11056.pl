Stephen Cole Kleene
Stephen Cole Kleene ( ur .
5 stycznia 1909 r. w Hartford w stanie Connecticut , zm .
25 stycznia 1994 r. w Madison w stanie Wisconsin ) – amerykański matematyk , jeden z pionierów informatyki teoretycznej .
Zasłynął z prac z teorii rekursji , opracowania koncepcji wyrażeń regularnych i teorii funkcji obliczalnych .
Z jego nazwiskiem związane są takie pojęcia jak domknięcie Kleene'ego , algebra Kleene'ego , twierdzenie Kleene'ego o rekursji oraz twierdzenie Kleene'ego o punkcie stałym .
Wniósł również wkład do prac intuicjonistów w matematyce .
Życiorys .
Kleene uzyskał bakalaureat w roku 1930 w koledżu Amherst .
W latach 1930-1935 studiował i pracował na Uniwersytecie w Princeton , gdzie w roku 1934 obronił dysertację `` A Theory of Positive Integers in Formal Logic '' , pisaną pod kierunkiem Alonzo Churcha .
Od roku 1935 pracował na wydziale matematyki uniwersytetu Wisconsin-Madison , a w roku 1937 uzyskał tytuł assistant professor .
W latach 1939 i 1940 był gościnnie wykładowcą w Institute for Advanced Study , gdzie pracował nad podstawami teorii rekursji .
W roku 1941 wrócił do Amherst , objął tam stanowisko wykładowcy matematyki .
Podczas II wojny światowej Kleene był porucznikiem w Marynarce Wojennej USA .
Początkowo pracował jako instruktor nawigacji , a następnie jako kierownik w Laboratorium Badawczym Marynarki .
Po wojnie wrócił do Wisconsin , gdzie otrzymał pełny stopień profesora w roku 1948 .
W latach 1969-1974 był tam dziekanem kolegium literatury i nauk .
W roku 1979 przeszedł na emeryturę .
W życiu prywatnym Kleene był aktywnym alpinistą .
Doceniał znaczenie przyrody i aktywnie wspierał wiele akcji prowadzonych przez jej obrońców .
