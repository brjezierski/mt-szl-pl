Stephen King
Stephen Edwin King ( ur .
21 września 1947 w Portland ) – amerykański pisarz specjalizujący się w literaturze grozy .
W przeszłości wydawał książki pod pseudonimem Richard Bachman , raz jako John Swithen .
Jego książki rozeszły się w nakładzie przekraczającym 350 milionów egzemplarzy , co czyni go jednym z najbardziej poczytnych pisarzy na świecie .
Jest autorem horrorów , które przeszły do klasyki gatunku , jak np .
`` Lśnienie '' , `` Smętarz dla zwierzaków '' ( w innym wydaniu jako `` Cmętarz zwieżąt '' ) , `` Miasteczko Salem '' , `` Podpalaczka '' .
Nie ogranicza się jednak do jednego gatunku , czego przykładem są : `` Cztery pory roku '' , `` Zielona mila '' , `` Oczy smoka '' , `` Bastion '' oraz 8-tomowy cykl powieści fantastycznych `` Mroczna Wieża '' .
Jest wielokrotnym zdobywcą Nagród Brama Stokera i British Fantasy Award .
W 2003 został odznaczony prestiżowym Medalem za Wybitny Wkład w Literaturę Amerykańską ( Medal for Distinguished Contribution to American Letters , DCAL ) przez National Book Foundation , a w 2014 – Narodowym Medalem Sztuki .
Stephen King ma córkę i dwóch synów , również pisarzy : Owena Kinga oraz Joe Hilla .
Zanim odniósł sukces jako pisarz , pracował jako nauczyciel języka angielskiego w szkole .
Życiorys .
Wczesne lata .
Kiedy King miał 3 lata , jego ojciec , Donald Edwin King , opuścił rodzinę .
Jego matka , Nellie Ruth Pillsbury , wychowała samotnie syna i jego adoptowanego starszego brata – Davida .
Często mieli problemy finansowe .
Przeprowadzili się do domu rodzinnego Ruth , do Durham w stanie Maine , ale mieszkali czasowo w Fort Wayne , w stanie Indiana oraz w Stratford , stan Connecticut .
Jako dziecko , King był świadkiem nieszczęśliwego wypadku – jeden z jego przyjaciół został potrącony przez pociąg i zmarł .
King uczęszczał do Durham Elementary School i Lisbon Falls High School ( matura 1966 ) , w stanie Maine .
Zaczął pisać bardzo wcześnie .
