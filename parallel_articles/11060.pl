Skórnik gałązkowy
Skórnik gałązkowy ( `` Stereum ochraceoflavum '' ( Schwein . )
Sacc .
– gatunek grzybów należący do rodziny skórnikowatych ( `` Stereaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Stereum , Stereaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1832 r. L.D von Schweinitz , nadając mu nazwę `` Thelephora ochraceoflava '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1888 r. P. A. Saccardo , przenosząc go do rodzaju `` Stereum '' .
Synonimy :
Nazwę polską podał S. Domański w 1991 r. Autor ten po raz pierwszy w Polsce opracował szczegółowo rodzinę skórnikowatych .
Władysław Wojewoda w swoim opracowaniu z 2003 r. podaje dla skórnika gałązkowego nazwę `` Stereum complicatum '' ( Fr . )
Fr. , jednak według Index Fungorum jest to odrębny gatunek .
