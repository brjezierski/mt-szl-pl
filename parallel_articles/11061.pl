Alfabet śląski Steuera
Alfabet śląski Steuera , ortografia Steuera ( śl .
: `` Steuerowy szrajbůnek '' ) – zmodyfikowany alfabet łaciński stworzony w okresie międzywojennym przez doktora Feliksa Steuera , służący początkowo do zapisu dialektu sułkowskiego , obecnie w swej zmodyfikowanej wersji stanowi propozycję ortografii skodyfikowanego języka śląskiego .
Alfabet ten składa się z 30 znaków :
A , B , C , Ć , D , E , F , G , H , I , J , K , L , Ł , M , N , Ń , O , P , R , S , Ś , T , U , Ů , W , Y , Z , Ź , Ż .
oraz dziesięciu dwuznaków :
AU , CH , CZ , DZ , DŹ , DŻ , OU , RZ , SZ , UO .
Dwuznak `` ou '' został dodany , aby oddać wymowę kontynuanta dawnego a pochylonego na Śląsku zachodnim ( jest on używany w miejsce południowośląskiego `` au '' i ogólnośląskiego `` o '' ) .
Proteza `` u- '' przed o oznacza prelabializacyjną wymowę krótkiego [ w ] przed każdym nagłosowym `` o '' .
Charakterystyczną cechą alfabetu Steuera jest zmiękczanie spółgłosek przez j , np .
`` bjoły '' ( biały ) , lub przez osobną literę , np .
ćymny ( ciemny ) .
Inną wprowadzoną zmianą w ortografii Steuera jest wprowadzenie apostrofu dla skróconych form czasownika , np .
`` wźůn'ech '' ( od wźůn żech ) .
W Steuerowym zapisie mowy śląskiej powstały m.in .
dzieła Feliksa Steuera : `` Ostatńi gwojźdźaurz '' ( 1934 ) , `` Z naszej źymjy ślůnskej '' ( 1934 ) oraz tomik poezji Karola Gwoździa : `` Myśli ukryte '' ( 2010 ) i wybór poezji Petra Murianki w przekładzie Bartłomieja Wanota `` Łymkowsko duszyczka '' ( 2022 ) .
