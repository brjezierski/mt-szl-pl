Steve Jobs
Steven Paul Jobs ( ur .
24 lutego 1955 w San Francisco , zm .
5 października 2011 w Palo Alto ) – jeden z trzech założycieli , były prezes i przewodniczący rady dyrektorów Apple Inc .
Był jedną z pierwszych osób , która zauważyła potencjał tkwiący w wynalazku laboratoriów przedsiębiorstwa Xerox , czyli środowisku graficznym i myszy komputerowej , dzięki czemu sukces odniosły później komputery Macintosh oraz ich graficzny system operacyjny Mac OS .
Jobs był twórcą przedsiębiorstwa NeXT Inc. ( zostało przejęte przez Apple Computer Inc , obecnie Apple Inc. ) .
