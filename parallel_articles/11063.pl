Steve Mandanda
Steve Mandanda ( wym .
; ur .
28 marca 1985 w Kinszasie ) – francuski piłkarz kongijskiego pochodzenia występujący na pozycji bramkarza we francuskim klubie Stade Rennais .
W latach 2008–2022 reprezentant Francji .
Kariera klubowa .
W lidze Mandanda zadebiutował w 2005 roku w barwach Le Havre AC , miał wówczas 20 lat .
Pierwszą bramkę puścił dopiero w czwartym spotkaniu .
W sezonie 2005/06 rozegrał w sumie 30 spotkań , zaś w kolejnym 37 .
6 lipca 2007 roku pewna strona internetowa ogłosiła , że zawodnik wybrał się na testy do Aston Villi .
Jednak szefowie klubu z Birmingham nie podpisali umowy z Francuzem , ponieważ nie odpowiadały im wymogi stawiane przez władze Le Havre .
W 2007 roku przeszedł do Olympique Marsylia .
1 lipca 2016 roku został piłkarzem Crystal Palace .
Po roku wrócił do drużyny z Marsylii .
Od 6 lipca 2022 roku został zawodnikiem Stade Rennes .
Kariera reprezentacyjna .
Steve zaliczył kilka występów w reprezentacji Francji do lat 21 .
W 2006 roku zaś sięgnął po Mistrzostwo Europy U-21 ze swoją reprezentacją .
