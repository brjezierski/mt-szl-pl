Stevenage
Stevenage – miasto i dystrykt w Wielkiej Brytanii ( Anglia ) , w hrabstwie Hertfordshire .
Liczba ludności wynosi około 84 tysięcy mieszkańców .
Miasto położone jest pomiędzy Letchworth Garden City i Welwyn Garden City , na wschód od 7 i 8 zjazdu z autostrady A1 ( M ) , 30 mil na północ od centrum Londynu .
Populacja w roku 1801 wynosiła 1430 mieszkańców , w 1901 roku – 4049 , w 2001 roku – 79 724 , w 2007 roku – 84 651 , a w 2011 roku – 83 957 .
Stevenage jest wspomniana w Domesday Book ( 1086 ) jako `` Stigenace '' .
9 sierpnia 1986 roku odbył się tu ostatni w historii koncert grupy Queen w oryginalnym składzie
