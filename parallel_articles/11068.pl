Stigmatomyces limosinae
Stigmatomyces limosinae Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Stigmatomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał Roland Thaxter w 1900 r. na owadzie `` Limosina fontinalis '' w USA .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w 1994 i 2003 r. opisał jego występowanie na muchówkach `` Rachispoda limosa '' i `` Rachispoda lutosa '' z rodziny Sphaeroceridae .
