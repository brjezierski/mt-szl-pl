Stigmatomyces purpureus
Stigmatomyces purpureus Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Stigmatomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1900 r. Roland Thaxter r. na owadzie `` Scatella stagnalis '' w USA .
Synonim :
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski opisał jego występowanie na muchówkach ( Diptera ) z rodziny wodarkowatych ( Ephydidrae ) : `` Scatella paludum '' , `` Scatella stagnalis '' , `` Scatella tenulcosta '' .
