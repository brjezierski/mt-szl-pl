Stigmina carpophila
Stigmina carpophila Speg .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , saprotrof i pasożyt , u drzew pestkowych wywołuje chorobę o nazwie dziurkowatość liści drzew pestkowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Stigmina , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1843 r. J.H .
Léveillé nadając mu nazwę `` Helminthosporium carpophilum `` .
Obecną , uznaną przez Index Fungorum nazwę nadał mu M.B .
Ellis w 1959 r .
Synonimy :
Morfologia .
Hodowany na agarze dekstrynowym tworzy szaro-brązowe kolonie .
Strzępki hialinowe , rozgałęzione , o szerokości 1,8-9 μm .
Konidiofory bladobrązowe , proste , nierozgałęzione , lub nieregularnie rozgałęzione , w miejscu przegród mają grubość 5-7,5 um .
Komórki konidiotwórcze cylindryczne , delikatnie brodawkowate .
Po oderwaniu się konidiów pozostają stożkowe blizny .
Konidia wrzecionowate , gładkie , z zaokrąglonym wierzchołkiem , o rozmiarach ( 22- ) 26-42 ( -75 ) × ( 10- ) 12-16 ( -18 ) um .
