Stilfs
Stilfs ( wł .
`` Stelvio '' ) – miejscowość i gmina we Włoszech , w regionie Trydent-Górna Adyga , w prowincji Bolzano ( Tyrol Południowy ) .
Liczba mieszkańców gminy wynosiła 1228 ( dane z roku 2009 ) .
Język niemiecki jest językiem ojczystym dla 97,66 % , włoski dla 2,26 % , a ladyński dla 0,08 % mieszkańców ( 2001 ) .
