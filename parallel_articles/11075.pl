Stobno ( województwo zachodniopomorskie )
Stobno ( niem .
`` Stöven '' ) – wieś w Polsce położona w województwie zachodniopomorskim , w powiecie polickim , w gminie Kołbaskowo .
We wsi odkryto zagrodę gospodarczą z epoki wczesnego neolitu – osadę datowaną na ok. 4600 r .
p.n.e. , zaliczaną do , której kolebka znajduje się pomiędzy Renem a Łabą ( wyniki archeologicznych badań Marcina Dziewanowskiego z UAM Poznań ) .
Wieś była wzmiankowana w 1243 roku , gdy Barnim I , książę szczeciński i pomorski z dynastii Gryfitów , przekazał ją na własność klasztorowi żeńskiemu ze Szczecina .
