Stockerau
Stockerau – miasto w Austrii , w północnej części kraju związkowego Dolna Austria , w powiecie Korneuburg .
Leży na wschodnim krańcu Kotliny Tullneńskiej , nad meandrami i starorzeczami Dunaju , w odległości ok. 25 km na północny zachód od Wiednia .
Liczy 16 127 mieszkańców ( 1 stycznia 2014 ) .
W mieście rozwinął się przemysł samochodowy , elektrotechniczny , drzewny , poligraficzny , tworzyw sztucznych oraz spożywczy .
