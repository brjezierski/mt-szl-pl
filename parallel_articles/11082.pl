Stodoły-Wieś
Stodoły-Wieś – wieś w Polsce , położona w województwie świętokrzyskim , w powiecie opatowskim , w gminie Wojciechowice .
W latach 1975–1998 miejscowość położona była w województwie tarnobrzeskim .
Historia .
Stodoły to stara wieś królewska należąca w przeszłości do starostwa sandomierskiego .
Nie mamy dokładnych informacji , kiedy powstała .
1 sierpnia 1944 r. patrol partyzancki , pod dowództwem Franciszka Ryczana `` Zefira '' , zlikwidował w Stodołach 5 własowców , którzy rabowali ludność polską .
W czasie przeprowadzonej w 1944 operacji lwowsko-sandomierskiej w okolicach Stodół toczyły się walki między armią niemiecką a Armią Czerwoną na tzw .
przyczółku baranowsko-sandomierskim .
Żołnierze armii radzieckiej polegli w walkach zostali pochowani na miejscowym cmentarzu .
