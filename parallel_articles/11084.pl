Stoke-on-Trent
Stoke-on-Trent – miasto i dystrykt ( unitary authority ) w Wielkiej Brytanii ( Anglia ) , w hrabstwie ceremonialnym Staffordshire , nad rzeką Trent .
Zostało utworzone przez federację sześciu miast w 1910 r .
W 2011 roku dystrykt liczył 249 008 mieszkańców .
Miasto jest ważnym ośrodkiem przemysłu ceramicznego , stanowiąc centrum regionu zwanego The Potteries .
