Stolin
Stolin ( biał .
`` Сто́лін '' ) – miasto na Polesiu na Białorusi , nad Horyniem , w obwodzie brzeskim , siedziba władz rejonu stolińskiego , około 15 km od granicy z Ukrainą ; do 1945 siedziba powiatu stolińskiego w województwie poleskim , 12 400 mieszk .
( 2010 ) .
Historia .
Stolin powstał w sercu Polesia , nad Horyniem .
Pierwsza wzmianka o mieście pochodzi z 1555 .
Stolin zapisał się w dziejach klęską wojsk litewskich w r. 1655 w potyczce z armią moskiewską pod wodzą kniazia Wołkońskiego .
Pobliski majątek w Mańkiewiczach ( teraz w granicach miasta ) należał do Radziwiłłów od XVI w. do 1939 .
Miasto położone było w końcu XVIII wieku w powiecie pińskim województwa brzeskolitewskiego .
Do przełomu XIX i XX w. był tu folwark ordynacji dawidgródeckiej .
Żona Antoniego Radziwiłła Maria Dorota w 1885 założyła 50–hektarowy park krajobrazowy w lesie na skarpie doliny rzeki Horyń , a w 1905 wybudowano wspaniale wyposażony neobarokowy pałac z wieżą .
Spalony w czasie II wojny światowej , rozebrany , nie pozostał po nim ślad .
Syn Antoniego Stanisław Radziwiłł był adiutantem J. Piłsudskiego – zginął w 1920 .
