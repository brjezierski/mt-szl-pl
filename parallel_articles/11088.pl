Stonawa
Stonawa ( , ) – wieś gminna i gmina w Czechach , w kraju morawsko-śląskim , w powiecie Karwina .
Historycznie leży na Śląsku Cieszyńskim , dużą mniejszość stanowią Polacy ( 25,8 % w 2001 ) i Słowacy ( 7,7 % ) .
Miejscowość znajduje się w Ostrawsko-Karwińskim Zagłębiu Węglowym , funkcjonuje tu kopalnia ČSM , spółki akcyjnej OKD , zatrudniająca również licznych górników z Polski .
Geografia .
Miejscowość położona jest w Kotlinie Ostrawskiej , na wysokości ok. 190 m n.p.m .
Przez miejscowość przepływa rzeka Stonawka , lewy dopływ Olzy .
Na północy i wschodzie graniczy z Karwiną ( z Kopalniami i Darkowem na północy , a z Łąkami na wschodzie ) , na zachodzie z Suchą Górną , a na południu z Olbrachcicami .
