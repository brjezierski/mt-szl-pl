Allianz Arena
Allianz Arena – stadion piłkarski w północnej części Monachium w Niemczech .
Od sezonu 2005/2006 swoje mecze rozgrywa na nim Bayern Monachium , a także reprezentacja Niemiec .
Do sierpnia 2017 roku swoje mecze rozgrywał na nim również TSV 1860 Monachium .
Obecnie jest to jeden z najnowocześniejszych stadionów piłkarskich na świecie .
Nazwa stadionu pochodzi od jednego z jego sponsorów – niemieckiej firmy ubezpieczeniowej Allianz .
Podczas rozgrywek Ligi Mistrzów , z powodu przepisu UEFA mówiącego , iż w nazwie stadionu nie może być nazwy sponsora , stadion przyjmuje nazwę `` Fußball Arena München '' .
Konstrukcja .
Konstrukcja Allianz Areny i zastosowane tutaj rozwiązania architektoniczne i techniczne sprawiają , że stadion ten uznawany jest za jeden z najnowocześniejszych sportowych obiektów świata .
Arena została zaprojektowana przez Jacques Herzoga oraz Pierre de Meurona .
Całkowita pojemność stadionu wynosi 75 024 miejsc .
Stadion posiada 57 343 miejsc siedzących , 13 794 stojących ( tylko w rozgrywkach organizowanych przez DFB i meczach nie organizowanych pod egidą UEFA i FIFA ) , 2 152 dla osobistości i prasy ( w tym 104 miejsca dla VIP-ów ) , 1 374 w loży honorowej , 966 dla sponsorów i 165 dla niepełnosprawnych .
Jest to drugi co do wielkości obiekt piłkarski w Niemczech , po Signal Iduna Park w Dortmundzie ( 80 645 ) .
Stadion posiada trzy poziomy trybun i jest skonstruowany tak , że zapewnia doskonałą widoczność z każdego miejsca .
Na niższym poziomie może przebywać 24 776 widzów , na środkowym 23 634 a na wyższym 21 592 .
Stadion jest wyposażony w nowoczesne nadajniki GSM 900/1800 oraz UMTS .
Pod dachem po przeciwległych stronach za bramkami umieszczono dwa telebimy o powierzchni 100 m² każdy .
To co najbardziej wyróżnia tę konstrukcję w porównaniu z innymi obiektami sportowymi , to charakterystyczny dach i fasada stadionu .
Są one zbudowane z 2 874 bardzo cienkich , foliowych poduszek napełnionych suchym powietrzem o łącznej powierzchni 64 000 m² , stale pompowanych powietrzem .
Jest to największa foliowa konstrukcja świata .
Każda z poduszek może zostać oddzielnie podświetlona na biało , czerwono , bądź niebiesko , w różnych odcieniach tych barw .
Stadion jest podświetlany na kolory zgodne z barwami gospodarzy .
Podświetlenie stadionu kosztuje około 60 euro na godzinę , a ogromna moc do tego zużyta sprawia , że w przejrzystą noc Allianz Arenę można dostrzec nawet z odległych o 75 km austriackich górskich szczytów .
Stadion posiada rozbudowaną infrastrukturę .
Na jego terenie znajdują się m.in .
trzy żłobki przeznaczone dla kibiców i piłkarzy , dwa sklepy dla fanów – „ Megastore ” Bayernu liczący 800 m² oraz TSV 1860 – z 400 m² powierzchni sklepowej .
Poza tym wokół stadionu działa 28 kiosków z artykułami dla kibiców .
Znajdują się tu sale konferencyjne , pomieszczenia biurowe , także kilka restauracji , które zajmują łącznie powierzchnię ok. 6 500 m² .
Do stadionu należy także czteropoziomowy parking ponad 10 000 samochodów i jest to jednocześnie największy samochodowy parking w Europie .
Mistrzostwa Świata w Piłce Nożnej 2006 .
Allianz Arena była jednym z głównych stadionów Mistrzostw Świata w piłce nożnej w 2006 roku .
Podczas mundialu nosił on nazwę „ FIFA WM-Stadion München ” .
