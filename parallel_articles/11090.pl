Stoneham ( Kanada )
Stoneham – miasto w Kanadzie , w prowincji Quebec , w regionie Capitale-Nationale .
Miasto położone jest nad Rzeką Świętego Wawrzyńca .
Liczba mieszkańców Stoneham wyniosła 7106 w roku 2011 .
Język francuski jest językiem ojczystym dla 98,1 % , angielski dla 1,7 % mieszkańców ( 2006 )
W mieście rozgrywały się Mistrzostwa Świata w Snowboardzie 2013 .
W Stoneham urodziła się Laurie Blouin , kanadyjska snowboardzistka , wicemistrzyni olimpijska .
