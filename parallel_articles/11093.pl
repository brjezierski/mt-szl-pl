Żagwiak wielkopory
Żagwiak wielkopory , jamczatka wielkopora ( `` Podofomes mollis '' ( Sommerf . )
Gorjón ) – gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Podofomes , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy takson ten zdiagnozował w 1826 r. Søren Christian Sommerfelt nadając mu nazwę `` Daedalea mollis '' .
Potem zaliczany był do różnych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 2020 r. Sergio Pére Gorjón .
