Storlus
Storlus – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie chełmińskim , w gminie Papowo Biskupie .
Wieś liczy 371,99 hektarów powierzchni .
Historia .
Średniowiecze .
Pierwsze informacje o Storlusie pochodzą z 1255 roku .
W dokumentach wieś występowała pod nazwami Storloes ( 1255 i 1409 ) , Stulitz ( ok. 1400 ) , Sturluz , Storschewal , Starczewald , Storlusz ( 1570 ) .
W miejscowości wieś występowała do komturstwa starogrodzkiego .
W średniowieczu wybudowano niewielki gród na wyspie położonej na jeziorze Jeleniec .
