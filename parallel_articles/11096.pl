Pomarańczowiec bladożółty
Pomarańczowiec bladożółty ( `` Pycnoporellus alboluteus '' ( Ellis & amp ; Everh . )
Kotl .
& amp ; Pouzar ) – gatunek grzybów z rzędu żagwiowców ( `` Polyporales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pycnoporellus , Pycnoporellaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1895 roku Job Bicknell Ellis i Benjamin Matlack Everhart , nadając mu nazwę `` Fomes alboluteus '' .
Obecną , uznaną przez Index Fungorum nazwę , nadali mu w roku 1963 František Kotlaba i Zdeněk Pouzar przenosząc go do rodzaju `` Pycnoporellus '' .
Synonimy naukowe :
Stanisław Domański w 1965 r. nadał polską nazwę oranżowiec bladożółty .
Władysław Wojewoda w 2003 r. zaproponował nazwę pomarańczowiec bladożółty , która jest zgodna z aktualną nazwą naukową .
