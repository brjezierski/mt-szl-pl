Stowięcino
Stowięcino ( kaszb .
`` Stowicëno '' , niem .
`` Stojentin '' ) – wieś sołecka w Polsce położona w województwie pomorskim , w powiecie słupskim , w gminie Główczyce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
Miejscowość położona na południowy wschód od Główczyc , 41,5 km od Słupska .
`` Stoientin '' ( 1402 , 1608 ) , `` Stojenthin '' , w zapisie : „ Boreslaff Stouwentine ” ( 1472 ) , „ Merten Stowentin ” ( 1479 ) .
Nazwa dzierżawcza z formantem -ino od kaszubskiej postaci spieszczonego imienia Stojęta jako Stowięta .
W pobliżu leży wieś Stojcino ( gm .
Smołdzino ) i spotykane zapisy historyczne mogą zatem odnosić się do jednej lub drugiej miejscowości .
W 1954 w Stowięcinie urodził się Andrzej Lepper , polityk i związkowiec , były wicepremier , przewodniczący Samoobrony Rzeczpospolitej Polskiej .
