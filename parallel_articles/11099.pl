Strabla ( stacja kolejowa )
Strabla – stacja kolejowa w Strabli , w województwie podlaskim , w Polsce .
W roku 2017 stacja obsługiwała 20–49 pasażerów na dobę .
Dworzec , o konstrukcji drewnianej , jest zamknięty .
Jeden peron wyspowy .
Stacja posiada trzy tory główne ( 1 , 2 i 3 ) oraz dwa tory ładunkowe ( 5 , częściowo rozebrany , oraz 7 ) .
