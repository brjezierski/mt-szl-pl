Straszęcin ( gmina )
Straszęcin ( od 1949 `` Żyraków '' ) – dawna gmina wiejska istniejąca w latach 1934-1948 w woj .
krakowskim i rzeszowskim ( dzisiejsze woj .
małopolskie ) .
Siedzibą władz gminy był Straszęcin .
Gmina zbiorowa Straszęcin została utworzona 1 sierpnia 1934 roku w powiecie ropczyckim w woj .
krakowskim z dotychczasowych jednostkowych gmin wiejskich : Bobrowa , Góra Motyczna , Grabiny , Korzeniów , Mokre , Nagoszyn , Straszęcin , Wiewiórka , Wola Wielka , Wola Żyrakowska , Zassów i Żyraków .
1 kwietnia 1937 roku powiat ropczycki został zniesiony , a z jego terytorium utworzono powiat dębicki .
Po wojnie gmina Straszęcin ( wraz z całym powiatem dębickim ) weszła w skład nowo utworzonego woj .
