Strategiczna gra czasu rzeczywistego ( ang .
`` real-time strategy '' , RTS ) – gatunek gier komputerowych , będący modyfikacją turowych gier strategicznych , polegającą na niekrępowaniu graczy turami i kolejkami ruchów .
Gracze mogą podejmować działania , kiedy chcą , natomiast skutki owych są widziane natychmiast .
Gatunek ten stawia większy nacisk na szybkość podejmowania decyzji .
Wśród gier RTS wyróżnia się gry taktyczne czasu rzeczywistego ( ang .
`` real-time tactical game '' , RTT ) , podgatunek lub odrębny gatunek charakteryzujący się większym naciskiem na decyzje taktyczne związane bliżej z samą walką niż gospodarcze decyzje strategiczne .
Jednym z najbardziej znanych przedstawicieli jest seria gier `` Myth '' .
