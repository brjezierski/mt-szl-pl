Streaking
Streaking ( ang .
od `` streak '' 'iść szybko , spieszyć się , biec ' ) – zdarzenie polegające na wbiegnięciu nago na imprezę masową , najczęściej sportową , by zwrócić na siebie uwagę widzów , i jak najdłuższym uciekaniu przed ochroną i policją .
Niekiedy `` streakerzy '' wypisują na swoim ciele różne hasła .
Streaking jest nielegalny i osoby uprawiające ten rodzaj aktywności są pociągane do odpowiedzialności karnej .
