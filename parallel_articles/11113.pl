Stresa
Stresa – miejscowość i gmina w północnych Włoszech , w regionie Piemont , w prowincji Cusio Ossola .
Położona jest na zachodnim brzegu jeziorem Maggiore , u podnóża szczytu Mottarone ( 1492 m n.p.m .
) .
Według danych na 31 maja 2021 gminę zamieszkiwało 4565 osób .
Miejscowość turystyczna , rozwijająca się intensywnie od początku XIX w. Słynie z widoków na jezioro Maggiore i położone w pobliżu cztery z pięciu Wysp Boromejskich ( Isola Bella , Scoglio della Malghera , Isola del Pescatori i Isola Madre ) .
W 1861 r. wybudowano tu słynny , istniejący do dziś Grand Hôtel des Iles Borromées .
Ze Stresy na szczyt Mottarone prowadzi dwuodcinkowa gondolowa kolej linowa Stresa - Mottarone , zbudowana w 1970 r. , unieruchomiona po wypadku z 23 maja 2021 r .
