Striptiz
Striptiz ( ang .
`` striptease '' ) – występ taneczny w kabarecie , częściej w rewii lub lokalu rozrywkowym , podczas którego tancerka lub tancerz w rytm muzyki rozbierają się aż do nagości .
Zazwyczaj jest to rodzaj pokazu erotycznego mającego na celu wywołanie podniecenia seksualnego .
Striptiz jest związany z pornografią i stanowi formę jej rozpowszechniania .
Charakterystyczną cechą striptizu jest stopniowanie napięcia poprzez wolniejsze zdejmowanie elementów odzieży w tych momentach , kiedy widownia oczekuje dalszego obnażania się .
Striptiz pojawił się na przełomie XIX i XX wieku jako element występów w teatrach burleski .
Pierwszy pokaz striptizu odbył się 1893 roku na balu w „ Moulin Rouge ” w Paryżu , gdzie modelka pracująca dla malarzy i innych artystów rozebrała się przed studentami , za co została ukarana grzywną .
Miało to spowodować rozruchy w paryskiej Dzielnicy Łacińskiej stłumione dopiero przez wojsko .
Współcześnie pokazy striptizu stanowią element wieczornego lub nocnego programu rozrywkowego w niektórych lokalach gastronomicznych .
Typem lokalu rozrywkowego , w którym striptiz stanowi podstawę funkcjonowania , jest klub go-go .
Zazwyczaj tancerką jest kobieta , choć zdarzają się także – szczególnie w lokalach odwiedzanych głównie przez kobiety , bądź w ramach imprez przeznaczonych dla kobiet – pokazy męskich tancerzy ( striptizerów ) .
Striptiz często prezentują prostytutki w agencjach towarzyskich , by zachęcić do skorzystania z ich usług .
Striptiz może być wykonywany podczas tańca na rurze .
W Polsce lokali ze striptizem ( nawet w dużych miastach ) jest stosunkowo niewiele .
W Europie lokali tego typu najwięcej jest w Paryżu i Pradze .
