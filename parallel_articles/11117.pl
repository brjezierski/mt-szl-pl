Góreczno
Góreczno ( daw .
`` Stroków '' , , ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek , na ziemi prudnickiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 51 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 8 km od granicy z Czechami , w zachodniej części Płaskowyżu Głubczyckiego .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Środowisko naturalne .
W Górecznie panuje klimat umiarkowany ciepły .
