Pierścieniak czarnozarodnikowy
Pierścieniak czarnozarodnikowy , łysiczka czarnozarodnikowa ( `` Stropharia melanosperma '' ( Bull . )
Gillet ) – gatunek grzybów z rodziny pierścieniakowatych ( `` Strophariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Stropharia , Strophariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungii '' .
Po raz pierwszy takson ten zdiagnozował w 1793 r. Jean Baptiste François Bulliard nadając mu nazwę `` Agaricus melanospermus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1878 r. Claude-Casimir Gillet .
Synonimy :
`` Agaricus melanospermus '' Bull .
1793
Nazwy polskie : pieczarka czarnozarodnikowa ( Franciszek Błoński 1896 ) , pierścieniak pomiotowy , bedłka pomiotowa ( Stanisław Chełchowski 1898 ) , łysiczka czarnozarodnikowa ( Władysław Wojewoda 2003 ) .
Zgodna z obecną nazwą naukową jest tylko nazwa pierścieniak pomiotowy , ale jest ona praktycznie nieznana .
