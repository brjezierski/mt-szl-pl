Stropkowo
Stropkowo – wieś w Polsce położona w województwie mazowieckim , w powiecie sierpeckim , w gminie Zawidz .
Leży nad Sierpienicą .
W latach 1975–1998 miejscowość administracyjnie należała do województwa płockiego .
2 września 2011 w miejscowości miał wypadek motocyklowy Jarosław Wałęsa .
Trafił do szpitala w stanie ciężkim .
