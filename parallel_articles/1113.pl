Almería
Almería ( wym . )
– miasto w południowej Hiszpanii , w regionie Andaluzja , stolica prowincji Almería , 411 km od Madrytu .
Liczy 194 203 mieszkańców ( 2015 ) i posiada 296,32 km² powierzchni .
Obecna nazwa pochodzi od arabskiego słowa `` Al-Mariyya '' , oznaczającego wieżę obserwacyjną .
Położona nad Morzem Śródziemnym , u podnóża Gór Betyckich .
Dzięki łagodnemu klimatowi ( średnia temperatura roczna 18,7 °C ) intensywna uprawa owoców ( przeważnie cytrusowych ) i warzyw .
Przemysł chemiczny , skórzano-obuwniczy , materiałów budowlanych oraz spożywczy .
Ważny ośrodek akademicki ( 15.000 studentów ) .
Port handlowy i rybacki .
Od lat 60 .
XX w. znaczący ośrodek turystyczno-wypoczynkowy , liczne kąpieliska na Costa del Sol .
W 2005 w Almeríi i jej okolicach rozgrywano XV Igrzyska Śródziemnomorskie .
Klimat .
Miasto leży w strefie klimatu subtropikalnego , na pograniczu półpustynnego i pustynnego ( klasyfikacja Köppena – `` BSh '' i `` BWh '' ) .
