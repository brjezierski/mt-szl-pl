Buty ( zespół muzyczny )
Buty – czeska rockowa grupa muzyczna .
Założona w Ostrawie , w roku 1986 z grup U238 i B komplex .
Do życia grupę powołali Richard Kroczek i Radek Pastrňák .
Do nich wkrótce dołączyli Vít Kučaj , Ivan Myslikovjan i Luděk Piásečný .
W muzyce grupy Buty miesza się bogactwo różnych stylów muzycznych ( m.in .
rock , folk , folklor , country , jazz , reggae ) z pomysłowymi a dowcipnymi tekstami .
Każdy ich nowy album różni się od poprzedniego .
Od kameralnego `` Pískej si pískej '' , do klimatycznego `` Ppoommaalluu '' ( część utworów wykorzystał Jan Svěrák w filmie Jazda ) , od przepełnionego pozytywnością `` Dřevo '' ( w tych dwóch albumach krytyka doszukuje się szczytu artystycznego grupy ) , do umiarkowanie popowego `` Rastakayakwanna '' .
Na ostatni album , `` Duperele '' , grupa kazała czekać sześć lat .
Ciekawostki .
Nazwa grupy została zaczerpnięta z gwar laskich ( w standardowym czeskim buty to `` boty '' ) .
To samo się tyczy nazwy ostatniego albumu , którego tytuł pochodzi z gwary ostrawskiej .
Za piosenki do filmu Jana Svěráka Jazda grupa otrzymała Czeskiego Lwa za najlepszą muzykę .
Piosenka `` Letí vrána '' , nawiązanie do Marcina Wrony , była puszczana na zakończenie audycji JW-23 .
