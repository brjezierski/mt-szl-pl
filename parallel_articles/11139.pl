Strzyga ( album )
Strzyga – album studyjny polskiego zespołu folkmetalowego Percival Schuttenbach .
Wydawnictwo ukazało się 7 października 2016 roku , nakładem wytwórni Sony Music Entertainment Poland .
Na płycie znaleźć można 13 utworów ( oraz jeden bonusowy – cover ) będących mieszanką utworów z poprzednich albumów grupy , oraz kompozycji całkowicie nowych .
Teksty i melodie utworów są zarówno inspirowane pieśniami ludowymi Słowian ( `` Pani Pana '' , `` Lazare '' , `` Aziareczka '' , `` Sokol mi leta vysoko '' ) jak i są tworzone w pełni przez muzyków .
Album został nagrany w kwietniu 2016 roku , w gdańskim studiu Custom34 .
