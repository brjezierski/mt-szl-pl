Strzyżów ( gromada )
Strzyżów – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Strzyżów z siedzibą GRN w Strzyżowie utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie hrubieszowskim w woj .
lubelskim na mocy uchwały nr 8 WRN w Lublinie z dnia 5 października 1954 .
W skład jednostki weszły obszary dotychczasowych gromad Ciołki , Hrebenne , Rogalin i Strzyżów ze zniesionej gminy Horodło w tymże powiecie .
Dla gromady ustalono 26 członków gromadzkiej rady narodowej .
1 stycznia 1962 do gromady Strzyżów włączono wieś i kolonię Husynne ze zniesionej gromady Teptiuków w tymże powiecie .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
