Republika Środkowoafrykańska
Republika Środkowoafrykańska ( , ) – państwo śródlądowe w środkowej Afryce , w dorzeczach rzek Konga i Szari .
Graniczy z Kamerunem , Czadem , Sudanem , Sudanem Południowym , Demokratyczną Republiką Konga i Kongiem .
1 grudnia obchodzone jest święto narodowe , Dzień Republiki , w rocznicę proklamowania republiki .
Geografia .
Krajobraz naturalny .
Kraj położony w środkowej części Afryki , prawie całkowicie na wyżynie Azande .
Wyżyna obniża się na północy w kierunku jeziora Czad , zmieniając się w płaskie , częściowo zabagnione tereny równinne .
W rejonie północno-zachodnim wznosi się masyw Yadé , a na północnym wschodzie Bongo .
Naturalną szatę roślinną kraju stanowią na południu wilgotne lasy równikowe zamieszkane przez wiele gatunków małp ( m.in .
przez goryle ) oraz na północy sawanna trawiasta z nielicznymi drzewami , będąca siedliskiem dla bawołów , słoni , antylop , hien .
W dolinach rzek znajdują się lasy galeriowe , w których żyją krokodyle i hipopotamy .
