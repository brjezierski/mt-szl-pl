Strážnice ( Góry Stołowe )
Strážnice ( pol .
`` Strażnica '' ) 696 m n.p.m .
- wzniesienie graniczne w południowo-zachodniej Polsce , w Sudetach Środkowych , w Górach Stołowych , w paśmie Zaworów .
Położenie .
Wzniesienie położone jest w północno-zachodniej części Zaworów , na granicy polsko-czeskiej , około 2,5 km na południowy wschód od centrum miejscowości Chełmsko Śląskie .
