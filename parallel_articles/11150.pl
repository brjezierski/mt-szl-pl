Strängnäs ( stacja kolejowa )
Strängnäs – stacja kolejowa w Strängnäs , w regionie Södermanland , w Szwecji .
Stacja została otwarta wraz z otwarciem Svealandsbanan w 1997 .
Stacja jest jednotorowa , dlatego nie ma możliwości mijanki pociągów na tej stacji .
Budynek dworca posiada poczekalnię , kiosk i toalety .
Planowana jest dobudowa drugiego toru i rozbudowa dworca kolejowego i zamienienie go na centrum podróży .
Prace planowane są na lata 2014-2017 .
