Styczeń
Styczeń jest miesiącem zimowym na półkuli północnej , a letnim na południowej .
Etymologia .
Jego nazwa , podobnie jak i pozostałych miesięcy ( z wyjątkiem marca i maja ) , pochodzi od charakterystycznych cech , związanych z towarzyszącymi temu miesiącowi zjawiskami przyrodniczymi lub z rytmem wykonywanych w tym czasie prac na roli .
Nazwa miesiąca ( według Brücknera ) pochodzi od słowa „ tyki ” , które w tym czasie sporządzali gospodarze .
Nazwa może też pochodzić od „ styku ” z uwagi na to , że miesiąc ten występuje na przełomie roku .
W języku staropolskim styczeń nazywano również `` tyczniem '' , `` godnikiem '' i `` ledniem '' ( por .
współczesna czeska nazwa miesiąca : `` leden '' ) .
Łacińska nazwa `` Ianuarius '' ( zobacz : kalendarz rzymski ) została zapożyczona przez większość języków europejskich , pochodzi od boga początku – Janusa .
