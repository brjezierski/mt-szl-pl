Stylopage grandis
Stylopage grandis Dudd .
– gatunek grzybów z rzędu zwierzomorkowców ( `` Zoopagales '' ) .
Grzyb drapieżny i koprofilny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum :
`` Stylopage , Zoopagaceae , Zoopagales , Incertae sedis , Zoopagomycetes , Zoopagomycotina , Zoopagomycota , Fungi '' .
Gatunek ten opisał w 1950 r. C.L .
Duddington w Anglii na nicieniu .
Morfologia i tryb życia .
Występuje w glebie , oraz w odchodach niektórych zwierząt .
Tworzy słabo rozgałęzione wegetatywne strzępki o szerokości około 5 μm pokryte bezbarwnym klejem .
Konidiofory wyprostowane , długości 300-500 µm , zawierające maksymalnie 2 konidia o owalnym kształcie .
Są to największe konidia w rodzaju `` Stylopage '' .
Mają długość 27-61 μm i szerokości 13-26 μm .
Zygospory nieznane .
Łowi drobne nicienie o długości do około 0,5 mm za pomocą strzępek pokrytych klejącym śluzem .
Gdy zwierzę dotknie jego strzępek przykleja się do szybko krzepnącego śluzu i zostaje uwięzione .
Ze strzępek do wnętrza ciała zwierzęcia wnikają ssawki grzyba trawiące jego ciało za pomocą enzymów .
