Alojzy Lysko
Alojzy Wiktor Lysko ( ur .
15 lutego 1942 w Bojszowach ) – polski pisarz związany ze Śląskiem , polityk i samorządowiec , nauczyciel górnictwa , poseł na Sejm V kadencji .
Życiorys .
Jego ojciec był wcielony do Wehrmachtu , zginął na froncie wschodnim .
Rodowity Ślązak .
Magister administracji po Uniwersytecie Śląskim .
W latach 1964–1992 ( do czasu przejścia na emeryturę ) pracował jako nauczyciel górnictwa , przez kilka lat był dyrektorem szkoły .
Jest autorem książek o tematyce historycznej i śląskiej , m.in .
publikacji `` To byli nasi ojcowie '' , poświęconej osobom przymusowo wcielonym do niemieckiego wojska .
Należał kolejno do Stronnictwa Konserwatywno-Ludowego i Przymierza Prawicy , z którym przystąpił do Prawa i Sprawiedliwości .
W latach 1998–2005 zasiadał w sejmiku śląskim .
W wyborach parlamentarnych w 2001 z listy PiS bez powodzenia kandydował do Sejmu , a w wyborach w 2005 uzyskał mandat poselski w okręgu katowickim .
Jako jedyny poseł PiS 7 września 2007 głosował przeciwko skróceniu kadencji Sejmu .
Początkowo deklarował brak zamiaru kandydowania w przedterminowych wyborach parlamentarnych , ostatecznie przyjął propozycję Platformy Obywatelskiej , z rekomendacji której bezskutecznie ubiegał się o mandat senatora .
Powołany później w skład rady programowej TVP Katowice .
Był członkiem komitetu poparcia Bronisława Komorowskiego przed wyborami prezydenckimi w 2010 i w 2015 .
W 2003 otrzymał Nagrodę im .
Wojciecha Korfantego przyznaną przez Związek Górnośląski , a w 2011 Śląską Nagrodę im .
Juliusza Ligonia .
W 2017 był nominowany do Górnośląskiej Nagrody Literackiej „ Juliusz ” za publikację `` Jak Niobe .
Opowieść Górnośląska '' .
W 2018 został laureatem śląskiej Nagrody im .
Karola Miarki .
