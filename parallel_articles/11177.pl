Suchary ( rejon grodzieński )
Suchary ( biał .
`` Сухары '' , ros .
`` Сухари '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Jeziory .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Berszty .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 277 osób , wśród których 276 było wyznania prawosławnego a 1 mojżeszowego .
Jednocześnie 276 mieszkańców zadeklarowało białoruską przynależność narodową a 1 żydowską .
Było tu 46 budynków mieszkalnych .
