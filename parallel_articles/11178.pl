Rytele Suche
Rytele Suche – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Ceranów .
Leży na lewym brzegu Bugu .
Zaścianek szlachecki Suche należący do okolicy zaściankowej Rytele położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Mieszkańcy wyznania rzymskokatolickiego należą do parafii Niepokalanego Poczęcia NMP w Ceranowie .
