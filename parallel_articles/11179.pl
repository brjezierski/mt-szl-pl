Suchedniów ( stacja kolejowa )
Suchedniów – stacja kolejowa w Suchedniowie , w województwie świętokrzyskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Stacja znajduje się przy ul .
Dworcowej , na wschód od centrum miasta .
W roku 2017 stacja obsługiwała 200–299 pasażerów na dobę .
Historia .
Budynek dworca pochodzi z XIX wieku , powstał podczas budowy kolei Iwanogrodzko-Dąbrowskiej .
Budynek przeszedł remont w 2014 roku , podczas którego wymieniono drzwi i okna , odnowiono elewację , pomalowano poczekalnię i klatkę schodową oraz zainstalowano pochylnię dla niepełnosprawnych .
