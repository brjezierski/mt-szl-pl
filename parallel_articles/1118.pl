Алёна Ланская , ros .
Алёна Ланская , właśc .
Alena Lapochina ( ur .
7 września 1985 w Mohylewie ) – białoruska piosenkarka .
Reprezentantka Białorusi w 58 .
Konkursie Piosenki Eurowizji ( 2013 ) .
Zasłużona Artystka Republiki Białorusi .
Życiorys .
Kariera .
W 2005 po raz pierwszy wystąpiła na Międzynarodowym Festiwalu Sztuk „ Słowiański Bazar ” w Witebsku .
W 2010 wygrała amerykański konkurs muzyczny `` Atlantic Breeze 2010 '' i wydała swój debiutancki album studyjny , zatytułowany `` Labirinty sudby '' .
W 2011 otrzymała pierwszą nagrodę na „ Słowiańskim Bazarze ” .
W tym samym roku uzyskała tytuł Zasłużonej Artystki Republiki Białorusi .
