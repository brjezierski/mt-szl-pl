Suchoręcz
Suchoręcz – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie nakielskim , w gminie Kcynia .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 214 mieszkańców .
Jest 21. co do wielkości miejscowością gminy Kcynia .
