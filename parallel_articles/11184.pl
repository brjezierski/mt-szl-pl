Wola Suchożebrska
Wola Suchożebrska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Suchożebry .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Marii Magdaleny w Suchożebrach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W miejscowości działa założona w 1954 roku jednostka ochotniczej straży pożarnej .
Jednostka posiada dwa samochody ratowniczo-gaśnicze : średni GBM 2/8 Star 200 , i lekki GLM 8 Volkswagen Transporter T4 .
