Do roku 1992 , czyli do wybuchu wojny gruzińsko-abchaskiej , było ważnym węzłem komunikacyjnym oraz centrum turystycznym , miastem wielokulturowym i wielonarodowościowym .
W powszechnym użyciu było dziewięć języków ( z abchaskim , gruzińskim i rosyjskim na czele ) .
W mieście działa kilka instytutów naukowych , z których najważniejszym jest Uniwersytet Abchaski .
W czasach sowieckich w Suchumi znajdowała się stacja hodowli małp .
W mieście prowadzono także badania nad bronią jądrową .
W latach 1945–1954 miejscowe laboratorium fizyki elektronowej zaangażowane było w program budowy radzieckiej bomby atomowej .
Historia .
Suchumi jest spadkobiercą greckiej kolonii Dioskuria , która została założona w VI w. p.n.e .
W okresie panowania rzymskiego i bizantyjskiego miasto nosiło nazwę Sebastopolis .
We wczesnym średniowieczu osada określano mianem `` Cchum '' , co w języku gruzińskim oznacza grab ( rodzaj drzewa ) .
Pełniła ona funkcję fortecy Księstwa Apsilae , a następnie Królestwa Abchaskiego , które pod koniec X wieku przeistoczyło się w Królestwo Gruzji .
