Suchumi
Suchumi ( , , , gr .
`` Dioskurias '' ) – `` de iure '' stolica republiki autonomicznej Abchazji wchodzącej w skład Gruzji , jednak `` de facto '' stolica państwa nieuznawanego .
Miasto jest ulokowane w zatoce , na wschodnim wybrzeżu Morza Czarnego .
Znane z sanatoriów , źródeł mineralnych i łagodnego , podzwrotnikowego klimatu .
W 2019 ludność miasta wynosiła nieco ponad 65 tys .
