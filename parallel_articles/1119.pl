Alor Setar
Alor Setar – miasto w Malezji , o bogatej , liczącej ponad 250 lat historii .
Jedno z najstarszych miast regionu , obecnie liczy ok. 400 000 mieszkańców .
Alor Setar jest stolicą malezyjskiego stanu Kedah oraz dystryktu Kota Setar .
Stanowi centrum dystrybucji dla produktów przemysłowych i rolniczych , a od momentu założenia miasta było stolicą Królestwa Kedah .
Położone jest 93 km na północ od Butterworth w stanie Pinang , a 45 km na południe od granicy Malezji z Tajlandią , współrzędne geograficzne : 6°07′N , 100°22′E .
