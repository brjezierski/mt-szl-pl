Sudan ( nosorożec )
Sudan ( ur .
1973 w Sudanie , zm .
19 marca 2018 w Kenii ) – ostatni samiec nosorożca północnego , podgatunku nosorożca białego .
Urodził się w 1973 , najprawdopodobniej na terenie Parku Narodowego Shambe w Sudanie ( obecnie Sudan Południowy ) , gdzie w 1975 został schwytany i przetransportowany do ogrodu zoologicznego w czechosłowackim mieście Dvůr Králové nad Labem , gdzie przebywał przez trzydzieści cztery lata .
Po tym , gdy okazało się , że jest jednym z ostatnich samców nosorożca północnego , zaś Zoo Dvůr Králové popadło w kłopoty finansowe , Sudan w grudniu 2009 trafił do rezerwatu Ol Pejeta – działającego od lat 40 .
XX wieku ośrodka dla dzikich zwierząt .
Rezerwat znajduje się w kenijskim hrabstwie Laikipia ( dawna prowincja Wielkiego Rowu ) , położonym na równiku .
Wraz z Sudanem przewieziono tam jeszcze trzy zwierzęta : dwie samice – jego córkę Najin i wnuczkę Fatu oraz samca Suniego .
