Maślak syberyjski
Maślak syberyjski , maślak amerykański forma syberyjska ( `` Suillus americanus '' ( Peck ) Snell ) – gatunek grzybów z rodziny maślakowatych ( `` Suillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Suillus , Suillaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1887 r. Charles Horton Peck nadając mu nazwę `` Boletus americanus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1944 r. Walter Henry Snell , przenosząc go do rodzaju `` Suillus '' .
Synonimy :
Nazwę maślak syberyjski nadała Alina Skirgiełło w 1960 r. W 2021 Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała używanie nazwy maślak amerykański forma syberyjska , odpowiadający taksonowi `` Suillus americanus '' ( Peck ) Snell f. `` sibiricus '' ( Singer ) Klofac .
Morfologia .
Średnica 5–10 cm , początkowo półkulisty , później tępostożkowaty z podwiniętym brzegiem , łukowaty , na koniec płaski z tępym garbem .
Powierzchnia śliska , początkowo słomkowożółta , potem brązowożółta z czerwonawymi plamkami .
