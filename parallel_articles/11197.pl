Suillus brevipes
Suillus brevipes ( Peck . )
Kuntze – gatunek grzybów z rodziny maślakowatych ( `` Suillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Suillus , Suillaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1885 r. Charles Horton Peck nadając mu nazwę `` Boletus brevipes '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1898 r. Otto Kuntze , przenosząc go do rodzaju `` Suillus '' .
Synonimyj :
Morfologia .
Kapelusz owocnika może osiągnąć średnicę 10 cm .
Wysokość trzonu to maksymalnie 6 cm .
Występowanie i siedlisko .
Występuje w Ameryce Północnej i Australii .
W Polsce nie występuje .
Żyje w mikoryzie z drzewami iglastymi , głównie z sosną wydmową i sosną żółtą .
Znaczenie .
Grzyb jadalny , bogaty w egzogenne kwasy tłuszczowe i kwas linolowy .
