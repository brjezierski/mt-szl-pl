Maślak rdzawobrązowy
Maślak rdzawobrązowy ( `` Suillus collinitus '' ( Fr . )
Kuntze ) – gatunek grzybów z rodziny maślakowatych ( `` Suillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Suillus , Suillaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1838 r. Elias Fries nadając mu nazwę `` Boletus collinitus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1898 r. Otto Kuntze .
