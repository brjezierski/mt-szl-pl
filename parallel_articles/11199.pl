Maślak daglezjowy
Maślak daglezjowy ( `` Suillus lakei '' ( Murrill ) A.H. Sm .
& amp ; Thiers ) – gatunek grzybów z rodziny maślakowatych ( `` Suillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Suillus , Suillaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1912 r. William Alphonso Murrill nadając mu nazwę `` Boletus lakei '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1964 r. Alexander Hanchett Smith i Harry Delbert Thiers , przenosząc go do rodzaju `` Suillus '' .
