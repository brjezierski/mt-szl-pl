Alpharetta
Alpharetta – miasto w Stanach Zjednoczonych , w stanie Georgia , siedziba hrabstwa Fulton .
Jest północnym przedmieściem w obszarze metropolitalnym Atlanty .
Na podstawie średniego dochodu mieszkańców miasto zostało uznane w 2022 roku za trzecie najbogatsze , obok Milton i Johns Creek w stanie Georgia .
Najwyżej oceniana szkoła średnia w Alpharetta , `` Chattahoochee High School '' , jest uznana przez US News za jedną z najlepszych szkół publicznych w kraju .
