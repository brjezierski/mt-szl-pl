Suillus pungens
Suillus pungens Thiers & amp ; A.H. Sm .
– gatunek grzybów z rodziny maślakowatych ( `` Suillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Suillus , Suillaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten zdiagnozowali w 1964 r. Thiers i A. H. Smith .
Morfologia .
Kapelusz owocnika ma kolor białawy , potem coraz bardziej szaro-oliwkowy , a później czerwonawo-brązowy i może osiągnąć średnicę 14 cm .
Wysokość trzonu to przeciętnie 7 cm , a grubość 2 cm .
Występowanie i siedlisko .
Występuje tylko w Nowej Zelandii , na Hawajach i nielicznych miejscach w Ameryce Północnej .
Żyje w mikoryzie z sosną kalifornijską i z `` Pinus muricata '' .
Grzyb rośnie często w okolicy grzyba `` Chroogomphus vinicolor '' .
