Dialekt sulkowski
Dialekt sulkowski ( także dialekt sułkowski , etnolekt śląski : `` sulkowski djalekt '' ) – jeden z dialektów śląskich , wyodrębniony przez Feliksa Steuera w swojej pracy `` Dialekt sulkowski '' ( 1934 ) .
Należy do dialektów pogranicza śląsko-laskiego , a swą nazwę bierze od rodzinnej miejscowości Steuera – Sułkowa .
W przeszłości utożsamiany z gwarami prudnickimi .
