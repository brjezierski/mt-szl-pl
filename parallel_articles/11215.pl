Sulęczyno
Sulęczyno ( dodatkowa nazwa w j. kaszub .
Sëlëczëno ; niem .
`` Sullenschin lub Sullenczin '' , daw .
`` Suleczyno '' ) – duża kaszubska wieś letniskowa w Polsce położona w województwie pomorskim , w powiecie kartuskim , w gminie Sulęczyno na Pojezierzu Kaszubskim , nad rzeką Słupią , przy trasie drogi wojewódzkiej nr 228 .
Miejscowość jest placówką Ochotniczej Straży Pożarnej i siedzibą sołectwa Sulęczyno ( w którego skład wchodzą również Bukowa Góra , Kłodno , Ostrów Mausz , Zimna Góra i Nowy Dwór ) .
Miejscowość jest siedzibą gminy Sulęczyno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Turystyka .
W pobliżu Sulęczyna znajdują się jeziora Mausz , Węgorzyno i Gowidlińskie .
Wieś jest punktem etapowym spływów kajakowych i ma rozwiniętą infrastrukturę turystyczną ( pensjonaty , ośrodki wypoczynkowe i agroturystyka ) .
Historia .
Pierwsze wzmianki o Sulęczynie pochodzą z roku 1224 .
Miejscowość należała początkowo do kasztelanii w Chmielnie .
W 1365 roku wieś stała się własnością Piotra z Rusocina , a następnie trafiła do rodziny Bellowów , której członkowie używali również nazwiska `` Sulęccy '' .
Od roku 1466 podlegała administracyjnie pod powiat mirachowski .
W 1585 Sulęczyno kupił prawnik i sekretarz króla Stefana Batorego Reinhold Heidenstein .
Wieś szlachecka położona była w II połowie XVI wieku w powiecie mirachowskim województwa pomorskiego .
