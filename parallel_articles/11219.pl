Sumartin
Sumartin – wieś w Chorwacji , w żupanii splicko-dalmatyńskiej , w gminie Selca .
Leży na wyspie Brač .
W 2011 roku liczyła 474 mieszkańców .
Została założona w 1645 roku przez pielgrzymów z Dalmacji , uchodzących przed Turkami .
Do dzisiaj jej mieszkańcy posługują się dialektem różniącym się od języka pozostałych mieszkańców wyspy .
Sumartin jest również portem , łączącym Brač ze stałym lądem ( połączenia do Makarskiej ) .
