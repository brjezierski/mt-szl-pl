( 6796 ) Sundsvall
( 6796 ) Sundsvall – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 195 dni w średniej odległości 2,74 j.a .
Została odkryta 21 marca 1993 roku w Europejskim Obserwatorium Południowym , w ramach programu `` Uppsala-ESO Survey '' .
Nazwa planetoidy pochodzi od szwedzkiego miasta Sundsvall .
Przed jej nadaniem planetoida nosiła oznaczenie tymczasowe ( 6796 ) 1993 FH24 .
