Sung Jae-ki
Sung Jae-gi ( kor .
성재기 , hancha : 成在基 , ur .
11 września 1967 w Daegu , zm .
26 lipca 2013 w Seulu ) – południowokoreański działacz praw człowieka i działacz obywatelski , antyfeminista , liberał , filozof .
W 2008 założył Stowarzyszenie koreańskiego mężczyzny ( kor .
남성연대 男 , hancha : 性連帶 ) .
Zginął , skacząc z mostu Mapo w Seulu .
