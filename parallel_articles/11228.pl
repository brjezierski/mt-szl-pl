Sunyani
Sunyani miasto w Ghanie , jest zarówno stolicą dystryktu Sunyani jak i regionu Brong Ahafo .
Szacunkowa populacja z roku 2005 to 80.245 mieszkańców , z tempem wzrostu 3.4 % rocznie .
Historia .
Sunyani otoczone jest zalesioną , południową częścią Wyżyny Aszanti , miasto powstało jako obóz dla myśliwych polujących na słonie w XIX stuleciu .
Nazwa Sunyani pochodzi od słowa `` Osono '' czyli słoń .
