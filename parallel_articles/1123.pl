Altamura
Altamura – miasto i gmina we Włoszech , w regionie Apulia , w prowincji Bari .
Według danych na styczeń 2009 gminę zamieszkiwało 69 214 osób przy gęstości zaludnienia 161,8 os./1 km² .
W 1993 w wapiennej grocie zwanej `` grotta di Lamalunga '' położonej w pobliżu miasta archeolodzy znaleźli prawie kompletny szkielet praczłowieka .
Szkielet położony jest na głębokości 60 m i uwięziony w naciekach stalagmitowych .
Wiek szkieletu oceniany jest na 130-170 tys .
