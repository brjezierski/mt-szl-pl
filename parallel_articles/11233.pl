Surabaja
Surabaja ( `` Surabaya '' ) – miasto w Indonezji , stolica prowincji Jawa Wschodnia , licząca razem z przedmieściami 5 mln mieszkańców .
Ekonomia .
Znaczący ośrodek przemysłowy i kulturalny .
Port morski ( `` Tanjungperak '' ) o dużym znaczeniu , w XVIII–XIX wieku największy w kraju ; międzynarodowy port lotniczy `` Juanda '' .
Most Suramadu łączy miasto z wyspą Madura .
Historia .
Pierwszy znany opis Surabai został sporządzony przez Holendrów w roku 1620 .
Surabaja według tego opisu była już wtedy dużym miastem otoczonym murem o 40-kilometrowej długości .
