Surakarta ( gra )
Surakarta – gra planszowa dla dwóch osób pochodząca z Indonezji .
Taką nazwę nadali jej Europejczycy , zapożyczając miano jawajskiego miasta Surakarta .
Plansza do gry składa się z 36 pół ( 6x6 ) , wyznaczonych przez przecinające się linie .
Niektóre z tych linii są przedłużone tworząc osiem charakterystycznych wiraży na rogach planszy .
Gra toczy się przy użyciu 24 pionów , po 12 dla każdego gracza , a jej celem jest zbicie wszystkich pionów przeciwnika .
