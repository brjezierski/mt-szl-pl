Suresnes
Suresnes – gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Demografia .
Według danych na rok 2012 miasto liczyło 47 263 mieszkańców , a gęstość zaludnienia wynosiła 12 470 osoby/km² .
Historia .
Tradycje winiarskie .
Już w czasach galorzymskich uprawiano winorośl na stokach Mont Valérien .
