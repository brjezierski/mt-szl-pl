Surrey ( Kanada )
Surrey – miasto w Kanadzie , w prowincji Kolumbia Brytyjska , w dystrykcie regionalnym Greater Vancouver .
Liczba mieszkańców Surrey wynosi 394 976 .
Język angielski jest językiem ojczystym dla 54,8 % , francuski dla 0,8 % mieszkańców ( 2006 ) .
Surrey to jedno z najszybciej rozwijających się miast kanadyjskich w latach 90. i następnej dekadzie .
„ Brama ” wjazdowa do aglomeracji Wielkiego Vancouveru ( `` Greater Vancouver District '' ) .
