Suva
Suva – stolica Fidżi ( od 1877 ) , położona na południowo-wschodnim wybrzeżu wyspy Viti Levu , w Dystrykcie Centralnym , w prowincji Rewa .
Stolica archidiecezji rzymskokatolickiej .
Ludność .
Według spisu powszechnego , który został przeprowadzony w roku 2007 , Suva liczy 85 691 mieszkańców , w tym na obszarze miejskim zamieszkuje 74 481 , a na obszarze podmiejskim 11 210 mieszkańców .
Pod względem liczby ludności jest drugim ( po Nasinu ) miastem kraju .
Stanowi centrum obszaru zurbanizowanego Suva , liczącego 173 137 mieszkańców .
Gospodarka .
Miasto jest ważnym ośrodkiem przemysłowym wysp ; rozwinięty jest tu przemysł spożywczy i stoczniowy .
Stanowi też główny port morski kraju , skąd wywozi się cukier , koprę i banany .
Ponadto znajduje się tu także port lotniczy .
Rozwija się turystyka .
Znajdują się tu siedziby giełdy papierów wartościowych , Reserve Bank of Fiji , Fiji School of Medicine i Uniwersytet Południowego Pacyfiku .
Chociaż Suva jest stolicą Fidżi , to jednak główny port lotniczy Fidżi znajduje się na przeciwległym brzegu wyspy , w Nandi .
Zabytki .
Do najważniejszych zabytków należy „ Grand Pacific Hotel ” z 1914 , wzniesiony w stylu wiktoriańskim .
Uniwersytet .
Uniwersytet Południowego Pacyfiku sfinansowały i zbudowały w 1968 wspólnie m.in .
: Fidżi , Tonga , Samoa , Vanuatu , Kiribati oraz Wyspy Salomona .
W 1985 kształcił 2300 studentów na 16 wydziałach .
