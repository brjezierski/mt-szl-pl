Suwon
Suwon ( kor .
`` 수원시 ; Suwon-si '' ) jest największym miastem i stolicą prowincji Gyeonggi w Korei Południowej .
Miasto położone jest około 30 kilometrów na południe od Seulu , na trasie kolejowej `` Gyeongbu '' i nowej linii superszybkich pociągów KTX .
Jest także przystankiem metra seulskiego .
Z centrum Seulu podróż metrem trwa około godziny .
Siedziba rzymskokatolickiej diecezji Suwon .
W mieście znajduje się hala sportowa Suwon Arena .
