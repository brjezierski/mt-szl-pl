Susiec ( stacja kolejowa )
Susiec – stacja kolejowa w Suścu ( osada Skwarki ) , w gminie Susiec , w powiecie tomaszowskim , w województwie lubelskim , w Polsce .
W roku 2017 stacja obsługiwała 10–19 pasażerów na dobę .
Do momentu likwidacji we wrześniu 2009 zatrzymywały się tu pociągi dalekobieżne relacji Zamość – Wrocław Główny ( całoroczny ) i Warszawa Zachodnia – Bełżec ( sezonowy ) spółki PKP Intercity .
Po przerwie pociągi pasażerskie powróciły do Suśca w 2011 , kiedy spółka Przewozy Regionalne uruchomiła sezonowe ( weekend majowy oraz wakacje ) , połączenia w relacji z Lublina i Zamościa do Bełżca .
Od 2012 ich trasa uległa wydłużeniu , wprowadzono bowiem także kursy w relacji z Lublina i Zamościa do Jarosławia .
Połączenia te są realizowane autobusami szynowymi .
W pobliżu stacji znajduje się nieczynny ośrodek wypoczynkowy w przeszłości należący do PKP .
