Swarga ( zespół muzyczny )
Swarga ( ros .
Сварга ) – założona w 1997 rosyjska grupa muzyczna wykonująca folk-pagan metal .
Od tego czasu grupa , poza licznymi koncertami , ma w dorobku trzy wydane płyty studyjne , dwa minialbumy oraz płytę demo .
Historia .
Svarga została założona pod koniec 1997 w podmoskiewskim mieście Żukowski .
Pierwotnym założeniem muzycznym było tworzenie doom metalu , co też grupa czyniła do 1999 , choć od początku w jej twórczości występowały motywy rodzimowierstwa słowiańskiego .
Sama nazwa grupy nawiązuje do mitycznej siedziby Swaroga , boga słońca .
Następnie pojawił się w warstwie muzycznej nowych utworów akordeon , co spowodowało zmianę stylu muzycznego na lżejszy pagan/folk metal .
W roku 2000 zespół własnym staraniem wydał płytę demo `` Через мертвую реку '' ( `` Czieriez miertwuju rieku '' , pol .
`` Przez martwą rzekę '' ) .
Mimo częstych zmian w składzie grupa aktywnie koncertowała w Moskwie i obwodzie moskiewskim , z takimi zespołami jak Arkona , Alkonost , Rossomahaar , Shadow Host i innymi .
Jesienią 2005 grupa wydała pierwszą w swym dorobku płytę , `` Огни на курганах '' ( `` Ogni na kurganah '' , pol .
`` Ognie na kurhanach '' ) , na której gościnnie jako wokalistka w kilku utworach wystąpiła Masza `` Scream '' Arichipowa z Arkony .
Po jej wydaniu grupa rozszerzyła działalność koncertową na inne miasta Rosji .
W 2007 wydała swoją drugą płytę , `` Там , Где Дремлют Леса '' ( Tam , gdzie drzemią lasy ) .
Najnowszym dziełem w dorobku zespołu jest album Кровь – Река ( Krow ’ – Rieka ) , który jest lżejszy od poprzednich , na nim znalazł się także utwór Смотри .
