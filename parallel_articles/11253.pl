Piestrzyca zatokowata
Piestrzyca zatokowata ( `` Helvella lacunosa '' Afzel . )
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Peziza , Pezizaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go Adam Afzelius w 1783 r. i nadana przez niego nazwa jest ważna do dzisiaj .
Synonimów naukowych ma ok. 50 .
Niektóre z nich :
Nazwa polska według M.A .
Chmiel .
Morfologia .
Średnica 2–7 cm , wysokość 2–5 cm .
Składa się z 2–3 powyginanych płatków o nieregularnym kształcie , które swoim dolnym brzegiem przyrośnięte są do trzonu .
Powierzchnia zewnętrzna jest gładka , naga , o barwie popielatej , siwej , niebieskosiwej , siwoczarnej , często z brązowawymi odcieniami .
Powierzchnia wewnętrzna również gładka , o barwie od siwej do sadzowosiwej .
Często zewnętrzna powierzchnia jest niemal czarna .
Wysokość 3–10 cm , średnica 1–3 cm .
Jest w środku pusty i ma kształt wąsko pałkowaty , walcowaty z podłużnymi żebrami , często również z głębokimi jamkami .
Powierzchnia gładka , barwy od siwej przez siwobrązową do czarniawej .
Cienki , kruchy , o barwie białawej lub szarawej .
Smak i zapach niewyraźny .
Zarodniki eliptyczne , gładkie z jedną dużą oleistą kroplą , szkliste , o rozmiarach 14,5–22 × 10–14 μm .
Wstawki szkliste , brązowego koloru , o granulowatej zawartości i rozmiarach 2,5–10 μm .
Występowanie i siedlisko .
Na półkuli północnej piestrzyca zatokowata jest szeroko rozprzestrzeniona , na półkuli południowej notowana tylko w Nowej Gwinei .
W Ameryce Północnej jest szeroko rozprzestrzeniona w Kalifornii , wzdłuż zachodniego wybrzeża oraz w górach .
W Polsce podano wiele jej stanowisk , ale na `` Czerwonej liście roślin i grzybów Polski '' ma status gatunku rzadkiego – potencjalnie zagrożonego z powodu ograniczonego zasięgu geograficznego i małych obszarów siedliskowych .
Rośnie na ziemi w lasach , najczęściej w świetlistych lasach liściastych , rzadziej w lasach iglastych , czasami także w zaroślach , na polanach , na składowiskach drzewa w lesie oraz na wypaleniskach .
Owocniki pojawiają się od maja do listopada , pojedynczo lub w niewielkich grupkach .
Znaczenie .
Prawdopodobnie jest grzybem mikoryzowym .
Czasami pasożytuje na niej `` Hypomyces cervinigenus '' oraz grzybnia lejkówki `` Clitocybe sclerotoidea '' .
Jest grzybem jadalnym .
