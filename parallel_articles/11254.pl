Ziemiozorek pomarszczony
Ziemiozorek pomarszczony ( `` Geoglossum umbratile '' Sacc . )
– gatunek grzybów z monotypowej rodziny `` Geoglossaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Geoglossum , Geoglossaceae , Geoglossales , Incertae sedis , Geoglossomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1878 r. Pier Andrea Saccardo .
Synonimy :
Polską nazwę zarekomendowała Komisja ds .
Polskiego Nazewnictwa Grzybów .
Morfologia .
Wysmukły , maczugowaty , czarniawy , o wysokości 2–10 cm , składający się z płodnej główki i sterylnego trzonu .
Główka wyraźnie wyodrębniona , o szerokości 4–7 mm , często spłaszczona , z jednym podłużnym rowkiem , czarna , w stanie suchym naga .
