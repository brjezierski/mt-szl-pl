Helvella queletii
Helvella queletii Bres .
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1882 r. Giacomo Bresàdola .
Johann Stangl w 1963 r. przeniósł go do rodzaju `` Paxina '' i na Index Fungorum jest pod nazwą `` Paxina queletii '' , jednak rodzaj ten według Index Fungorum jest synonimem rodzaju `` Helvella '' .
