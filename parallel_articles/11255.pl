Morfologia .
Składający się z kapelusza i trzonu .
Kapelusz o średnicy 2–6 cm i o specyficznym kształcie prawie półkulistej korony , otwartej , rozciągniętej lub bocznie ściśniętej .
Brzeg falisty , w młodości podwinięty , w stanie dojrzałym prosty .
