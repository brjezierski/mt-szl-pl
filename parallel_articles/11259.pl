Swilengrad
Swilengrad ( , ) – miasto w południowej Bułgarii , w obwodzie Chaskowo ; siedziba administracyjna gminy Swilengrad .
Według danych szacunkowych Ujednoliconego Systemu Ewidencji Ludności oraz Usług Administracyjnych dla Ludności , 15 czerwca 2020 roku miejscowość liczyła 18 502 mieszkańców .
Położenie .
Ulokowany przy granicy z Turcją i Grecją , znajduje się tu ważne drogowe i kolejowe przejście graniczne .
Swilengrad położony jest na międzynarodowej trasie kolejowej łączącej Stambuł z Sofią , Bukaresztem i Budapesztem .
Rozmieszczony jest po obu brzegach rzeki Maricy i nad Kanaklijską .
Klimat .
Temperatura w styczniu wynosi +2,5 stopnie Celsjusza , roczna amplituda wynosi poniżej 20 stopni Celsjusza .
