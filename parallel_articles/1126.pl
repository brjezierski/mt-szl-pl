Alternaria alternata
Alternaria alternata ( Fr . )
Keissl .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , u roślin wywołujący wiele chorób roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Alternaria , Pleosporaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Znana jest tylko anamorfa tego gatunku .
Na podstawie budowy molekularnej i ultrastrukturalnej ustalono , że teleomorfą jest któryś z gatunków grzybów zaliczany do rodzaju `` Lewia '' .
Po raz pierwszy takson ten zdiagnozował w 1832 roku Elias Fries nadając mu nazwę `` Torula alternata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1912 roku Karl von Keissler .
Synonimy :
Cechy mikroskopowe i biochemia .
Grzybnię łatwo można hodować na sztucznych pożywkach .
Na pożywce MEA w temperaturze 22–30 °C kolonie osiągają średnicę 6 cm po siedmiu dniach .
