Swindon ( stacja kolejowa )
Swindon – stacja kolejowa w mieście Swindon w hrabstwie Wiltshire na linii kolejowej Great Western Main Line .
Na stacji zatrzymują się pociągi pośpieszne .
Stacja jest również końcową dla bocznej linii Cheltenham Spa - Swindon .
Stacja w 2004 r. zdobyła nagrodę `` Station Excellence of the Year Award '' .
Ruch pasażerski .
Ze stacji korzysta 651 824 pasażerów rocznie ( dane za okres od kwietnia 2020 do marca 2021 ) .
Posiada bezpośrednie połączenia z Bristolem , Bath Spa , Londynem i Cheltenham Spa .
Pociągi odjeżdżają ze stacji w odstępach co najwyżej półgodzinnych w każdą stronę .
Obsługa pasażerów .
Automat biletowy , kasa biletowa , przystanek autobusowy , postój taksówek , bufet , kiosk .
Stacja dysponuje parkingiem samochodowym na 578 miejsc .
