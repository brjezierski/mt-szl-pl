Świrz
Świrz ( , za II RP także Swirz ) – wieś w rejonie przemyślańskim obwodu lwowskiego Ukrainy , nad rzeką Świrż .
Historia .
Miejscowość wzmiankowana po raz pierwszy w 1422 roku podczas rozgraniczenia wsi ze Stokami i Lubeszką .
Wieś prawa wołoskiego , położona była w pierwszej połowie XV wieku w ziemi lwowskiej województwa ruskiego .
W 1428 r. Świrz odwiedził król Władysław II Jagiełło .
W 1449 r. doszło do rozgraniczenia miasta i wsi o tych samych nazwach .
W 1484 r. bracia Marcin i Świrscy ufundowali tu parafię i pierwszy drewniany kościół .
Na jego miejscu w 1546 r. zbudowano w stylu renesansowym murowaną świątynię pw .
Wniebowzięcia Najświętszej Maryi Panny .
Przez pewien czas podczas zaboru austriackiego jako miasteczko wchodził w skład obwodu , czyli cyrkułu brzeżańskiego .
W II Rzeczypospolitej miejscowość była siedzibą gminy wiejskiej Swirz w powiecie przemyślańskim województwa tarnopolskiego .
