`` European Free Alliance '' , fr .
`` Alliance libre européenne '' ) ( WSE ) ( zwany również `` Europa Stu Flag '' ) jest europejską organizacją grupującą ugrupowania polityczne „ narodów bez państw ” .
Partie te żądają albo autonomii , albo pełnej niepodległości dla swoich narodów .
W Parlamencie Europejskim WSE tworzy wspólną grupę z Zielonymi pod nazwą Zieloni/WSE .
