Husky syberyjski
Husky syberyjski – jedna z ras psów , należąca do grupy szpiców i psów w typie pierwotnym , zaklasyfikowana do sekcji północnych psów zaprzęgowych .
Zgodnie z klasyfikacją amerykańską , należy do grupy psów pracujących .
Typ wilkowaty .
Nie podlega próbom pracy .
Historia .
Rasa ta jest rasą pierwotną , pochodzącą z rejonu Kołymy w północnej Syberii .
Hodowana tam przez pionierów w hodowli psów zaprzęgowych – Czukczów oraz Kamczadalów , Koriaków i Jukagirów .
Około 17 tysięcy lat temu wiele paleoazjatyckich plemion zamieszkiwało te tereny , gdy były one cieplejszym i bardziej przyjaznym terenem łowieckim .
Ludzie zdobywali tam pożywienie polując , w czym pomagały im psy myśliwskie .
Każda grupa stworzyła własny typ psa w zależności od wymagań łowieckich , ukształtowania terenu , występujących temperatur czy grubości pokrywy śnieżnej .
Czukcze żyli na półwyspie rozciągającym się z Syberii w kierunku Alaski .
Około 3000 lat temu klimat uległ znacznemu ochłodzeniu , toteż Czukcze zaczęli wykorzystywać psy do ciągnięcia sań .
Lud ten żył w stałych osadach , w głębi lądu i aby polować musiał przemierzać duże odległości .
Ideałem psa było niewielkie zwierzę , które potrzebowało mało pokarmu , ale było na tyle silne , aby ciągnąć upolowaną zdobycz z umiarkowaną prędkością na długich dystansach .
Kiedy zaistniała potrzeba przewiezienia cięższego ładunku , pożyczano psy od sąsiadów i zamiast 6-8 zaprzęgano 16 lub 18 .
Wszystkie samce , oprócz najlepszych , prowadzących zaprzęgi , kastrowano .
Zabieg ten miał na celu nie tylko ukrócenie zbędnych kojarzeń , ale pomagał psom w utrzymaniu tłuszczu na ciele .
Czukockie zaprzęgi liczące 20 psów potrafiły podobno przebyć w ciągu 6 godzin odległość 120 km i ciągnąć ciężar o masie około 600 kg .
Były one często używane przez Rosjan do transportu pasażerów i poczty .
Pokonywały wówczas 160–180 km dziennie w szybkim tempie .
Husky zostały sprowadzone do Kanady w 1909 r. przez rosyjskiego handlarza futrami Williama Goosaka , ale nie jest wykluczone , że mogły się tam przedostać znacznie wcześniej .
Goosak wystawił zaprzęg w wyścigu „ All Alaska Sweepstake ” na dystansie 650 km .
W porównaniu z miejscowymi psami zaprzęgowymi wydawały się być tak niepozorne , iż początkowo zostały wyśmiane i ochrzczone mianem „ syberyjski szczur ” .
Zaprzęg owych „ szczurów ” , prowadzony przez Thurnstrup ' a , po pokonaniu morderczego dystansu zjawił się na mecie jako trzeci .
I tak rozpoczął się ciąg zwycięstw psów tej rasy .
W 1910 r. na trasie tego samego wyścigu John Johnson ze swym zaprzęgiem husky uzyskał rekordowy czas 74 h. 14 min .
39 sek. , pobity dopiero niedawno , w 1983 r. W latach 1915 , 1916 i 1917 wygrywał słynny zaprzęg husky Leonarda Seppali .
Psy Seppali dały początek hodowli tej rasy na kontynencie amerykańskim , gdzie jako szybkie i wytrzymałe prędko zyskały sławę i uznanie .
Rozwój hodowli doprowadził do zarejestrowania rasy przez „ Amerykański Kennel Club ” .
Krzyżówki z miejscowymi psami doprowadziły do uzyskania typu uniwersalnego .
Psów używano nie tylko w sporcie , ale również w pracy – do przewożenia niezbyt ciężkich ładunków i poczty .
Norweski badacz Roald Amundsen ( 1872–1928 ) , planując ekspedycję naukową z Alaski na Biegun północny , wybrał do transportu sprzętu właśnie husky .
Niestety , wybuch I wojny pokrzyżował plany ekspedycji , a sprowadzone przez Amundsena psy rozpowszechniły się po Kanadzie i USA .
Kiedy w latach 1950–1960 rozkwitła hodowla husky w Ameryce Północnej przywieziono pierwsze egzemplarze tych psów do Europy .
Popularność oraz liczebność psów rasy siberian husky w Europie znacznie wzrosła wraz z rozwojem sportu zaprzęgowego .
Użytkowość .
Współcześnie husky syberyjski jest wykorzystywany , tak jak i dawniej , jako pies zaprzęgowy , także jako pies rodzinny .
Wygląd .
Husky syberyjski to pies silny umiarkowanie , zwarty , o sportowej sylwetce formatu skróconego prostokąta .
Szata i umaszczenie .
Dopuszczalne są wszystkie rodzaje umaszczenia , od czysto białego do jednolicie czarnego , jednak najczęściej spotykane zestawienia barw to : czarny , szary , wilczasty , srebrzysty , rudy , czekoladowy i złocisty z bielą , także pinto ( łaciate ) .
Na głowie występuje duża różnorodność rysunku , tzw .
maski – z reguły biała kufa i część twarzowa czaszki , reszta barwna .
Włos jest dwojakiego rodzaju – tworzą go podszerstek i włos okrywowy średniej długości ; bardzo gęsty , sprawia wrażenie obfitego , ale nigdy nie jest tak długi , żeby przesłaniał wyraźny rysunek linii psa .
Zdrowie i pielęgnacja .
Sierść husky wymaga szczotkowania dwa razy w tygodniu .
Dwa razy w roku , kiedy wełnisty podszerstek husky wypada płatami , pies potrzebuje wzmożonej pielęgnacji .
Najbardziej typowe dla rasy dolegliwości to choroby oczu ( w tym postępujący zanik siatkówki ) , dysplazja bioder i niedobory cynku .
Długość życia : 12 do 14 lat .
