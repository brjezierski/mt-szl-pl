Sycylia
Sycylia ( , ) – największa wyspa na Morzu Śródziemnym ( ponad 25 tys .
km² ) , położona we Włoszech na południe od Półwyspu Apenińskiego , od którego oddziela ją wąska Cieśnina Mesyńska .
Od południa oddzielona Cieśniną Maltańską od Malty .
Razem z Wyspami Liparyjskimi , Egadami , Wyspami Pelagijskimi i Pantellerią tworzy od 1946 roku specjalny region autonomiczny ( o powierzchni 25,7 tys .
km² i 5,1 mln ludności ) ze stolicą w Palermo .
Najwyższym wzniesieniem na wyspie jest Etna ( 3323 m n.p.m . )
– najwyższy aktywny wulkan na terenie Europy .
Nazwa .
Starożytni Grecy nazywali wyspę `` Trinacrią '' ( τρεῖςἄκρα , od τρεῖ „ trzy ” i ἄκρα „ cypel ” ) , co było nawiązaniem do jej trójkątnego kształtu , wyznaczanego przez trzy cyple : Capo Peloro na północnym wschodzie , Capo Boeo na północnym zachodzie i Capo Passero na południowym wschodzie .
Starożytni Rzymianie nazywali wyspę `` Triquetrą '' .
Nazwa łacińska `` Sicilia '' ( pol .
Sycylia ) pochodzi od łacińskiej nazwy ludu zamieszkującego wschodnią część wyspy – `` Siculli '' ( pol .
Sykulowie ) .
Geografia .
Położenie i powierzchnia .
Sycylia położona jest w środkowej części Morza Śródziemnego , pomiędzy Morzem Tyrreńskim a Morzem Jońskim , od kontynentalnej części Włoch oddzielona jest Cieśniną Mesyńską ( o szerokości 3 km na północy i 16 km na południu ) , a od Afryki Cieśniną Sycylijską o szerokości 148 km .
Z powierzchnią 25,7 tys .
