Alternaria brassicae
Alternaria brassicae ( Berk . )
Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Alternaria , Pleosporaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1836 roku Miles Joseph Berkeley nadając mu nazwę `` Macrosporium brassicae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1880 roku Pier Andrea Saccardo .
Znana jest tylko anamorfa tego gatunku .
Na podstawie budowy molekularnej i ultrastrukturalnej ustalono , że teleomorfą jest któryś z gatunków grzybów zaliczany do rodzaju `` Lewia '' .
Synonimów ma ponad 40 .
