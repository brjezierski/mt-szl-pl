Wrośniak anyżkowy
Wrośniak anyżkowy ( `` Trametes suaveolens '' ( L. ) Fr . )
– gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Trametes , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1753 r. Karol Linneusz nadając mu nazwę `` Boletus suaveolens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1838 r. Elias Fries , przenosząc go do rodzaju `` Trametes '' .
Synonimów naukowych ma ok. 30 .
