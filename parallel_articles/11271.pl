Smolucha bukowa
Smolucha bukowa ( `` Ischnoderma resinosum '' ( Schrad . )
P .
Karst . )
– gatunek grzybów z rodziny `` Ischnodermataceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ischnodermataceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1794 r. Schrader nadając mu nazwę `` Boletus resinosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1879 r. Karsten , przenosząc go do rodzaju `` Ischnoderma '' .
