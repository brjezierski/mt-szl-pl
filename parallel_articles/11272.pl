Pismaczek Mougeota ( `` Alyxoria mougeotii '' ( A .
Massal . )
Ertz , Frisch & amp ; G. Thor ) – gatunek grzybów z rodziny `` Lecanographaceae '' .
Ze względu na współżycie z glonami zaliczany jest także do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lecanographaceae , Arthoniales , Arthoniomycetidae , Arthoniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1853 r. Abramo Bartolommeo Massalongo nadając mu nazwę `` Opegrapha mougeotii '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2014 r. Ertz , Frisch i G. Thor , przenosząc go do rodzaju `` Alyxoria '' .
