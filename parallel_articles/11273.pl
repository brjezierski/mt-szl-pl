Sydney Pollack
Sydney Irwin Pollack ( ur .
1 lipca 1934 w Lafayette , zm .
26 maja 2008 w Los Angeles ) – amerykański reżyser , aktor i producent filmowy .
Życiorys .
Wczesne lata .
Sydney Pollack urodził się w Lafayette w stanie Indiana , w rodzinie żydowskich imigrantów z Rosji – Rebeki ( z domu Miller ) oraz Davida Pollacka ; jego ojciec był półzawodowym bokserem i aptekarzem .
Gdy Pollack był dzieckiem , rodzina przeprowadziła się do South Bend , a rodzice rozwiedli .
Matka Pollacka była alkoholiczką z problemami emocjonalnymi i zmarła w wieku 37 lat , gdy Pollack był studentem .
Wbrew wcześniejszym planom , żeby pójść do college ’ u , a następnie szkoły medycznej , Pollack opuścił Indianę i przeprowadził się do Nowego Jorku niedługo po ukończeniu liceum w wieku 17 lat .
W latach 1952–1954 uczył się aktorstwa na nowojorskiej Neighbourhood Playhouse , gdzie wykładowcą był wówczas Sanford Meisner .
Po dwuletniej służbie wojskowej , w 1958 roku wrócił do szkoły na zaproszenie Meisnera i został jego asystentem .
W 1960 roku John Frankenheimer , kolega Pollacka , zaprosił go do Los Angeles i poprosił , żeby został nauczycielem aktorstwa aktorów dziecięcych występujących w pierwszej dużej produkcji Frankenheimera , `` The Young Savages '' .
W trakcie pracy nad tym filmem Pollack poznał Burta Lancastera , który namówił Pollacka , wówczas aktora , żeby zajął się reżyserią .
