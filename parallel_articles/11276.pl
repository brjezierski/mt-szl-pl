Izydorówka
Izydorówka ( ukr .
Сидорівка ) – wieś na Ukrainie .
Należy do rejonu żydaczowskiego w obwodzie lwowskim i liczy 296 mieszkańców .
Historia .
Pod koniec XIX w. część wsi Machliniec w powiecie stryjskim .
Za II Rzeczypospolitej do 1934 roku wieś stanowiła samodzielną gminę jednostkową w powiecie żydaczowskim w woj .
stanisławowskim .
W związku z reformą scaleniową została 1 sierpnia 1934 roku włączona do nowo utworzonej wiejskiej gminy zbiorowej gminy Ruda w tymże powiecie i województwie .
Po wojnie wieś weszła w struktury administracyjne Związku Radzieckiego .
Urodził się tu Konstanty Dzieduszycki ( ur .
4 lipca 1884 , zm .
29 maja 1964 w Darenth ) – polski ziemianin , polityk , poseł na Sejm i senator w II Rzeczypospolitej .
Doktor praw .
Adiutant Józefa Piłsudskiego .
