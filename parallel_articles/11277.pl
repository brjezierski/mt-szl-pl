Sydowia polyspora
Sydowia polyspora ( Bref .
& amp ; Tavel ) E. Müll .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sydowia , Dothioraceae , Dothideales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1891 r. Julius Oscar Brefeld i Franz von Tavel nadając mu nazwę `` Dothidea polyspora '' .
Przez długi czas gatunek ten znany był tylko w formie anamorfy jako `` Sclerophoma pithyophila '' .
W 1953 r. Emil von Müller zbadał , że jego teleomorfą jest `` Sydowia polyspora '' i dotychczasowa anamorfa została uznana za jej synonim .
Wszystkich synonimów tego gatunku jest 24 .
Charakterystyka .
Grzyb pasożytniczy wywołujący wiele grzybowych chorób roślin , m.in .
jesienną osutkę sosny i zamieranie wierzchołków jałowca .
Gatunek szeroko rozprzestrzeniony .
Występuje na niektórych wyspach i na wszystkich kontynentach poza Antarktydą .
Znany jest także w Polsce .
Jest endofitem .
Anamorfa w igłach sosny tworzy podkładki .
Pod skórką sosny powstają w nich oddzielnie lub gromadnie , jednokomorowe lub niedokładnie podzielone pyknidia o średnicy 0,2–0,3 mm .
Mają ścianki grubości kilku komórek , zbudowane z ciemnobrązowego pseudomiąższu sklerotioidalnego , którego wewnętrzne komórki są subhyalinowe i działają jak fialidy .
Brak wyraźnej ostioli , konidia wydostają się przez nieregularne pęknięcia górnej ściany pyknidium .
Konidiofory z końcową fialidową aperturą , 4–7 × 4–5 µm , hialinowe lub subhyalinowe , bez przegród , doliformalne .
Konidia szkliste , bez przegród , jajowate , 4-8 × 2-3 µm , zlepione śluzem .
