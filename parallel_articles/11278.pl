Syktywkar
Syktywkar ( ) – miasto w Federacji Rosyjskiej , stolica republiki Komi , nad Sysołą , w pobliżu jej ujścia do Wyczegdy .
Liczy około 243 tys .
mieszkańców ( 2021 ) .
Nazwa miasta pochodzi z języka komi i oznacza „ miasto na rzece Sysole ” .
Do roku 1930 miasto nazywało się Ust'-Sysolsk ( ros .
`` Усть-Сысольск '' ) .
Miasto zostało założone w 1586 roku .
