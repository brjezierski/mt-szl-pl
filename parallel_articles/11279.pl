Sylacauga ( meteoryt )
Meteoryt z Sylacauga – meteoryt z grupy chondrytów zwyczajnych , który spadł 30 listopada 1954 w Oak Grove w pobliżu Sylacauga w Alabamie o godzinie 14:46 czasu lokalnego ( 18:46 UT ) .
Meteoryt znany jest także jako Hodges meteorite , ponieważ jeden z jego fragmentów uderzył w 31-letnią Ann Elizabeth Hodges .
Przelot i rozpad bolidu .
Meteoroid wszedł w atmosferę ziemską po słonecznej stronie Ziemi i poruszał się od strony Słońca .
Analiza przybliżonej orbity wskazuje , że odłamek skalny mógł pochodzić z planetoidy ( 1685 ) Toro .
