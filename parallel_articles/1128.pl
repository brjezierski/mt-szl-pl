Alternaria brassicicola
Alternaria brassicicola ( Schwein . )
Wiltshire – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Alternaria , Pleosporaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1832 roku Lewis Schweinitz nadając mu nazwę `` Helminthosporium brassicicola '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1947 roku S.P .
Wiltshire .
Znana jest tylko anamorfa tego gatunku .
Na podstawie budowy molekularnej i ultrastrukturalnej ustalono , że teleomorfą jest któryś z gatunków grzybów zaliczany do rodzaju `` Lewia '' .
