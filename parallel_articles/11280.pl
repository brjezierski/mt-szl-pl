Symferopol
Symferopol ( , , lub / , ) – miasto na Półwyspie Krymskim nad rzeką Sałhyr .
Miasto zamieszkuje 336 212 osób ( 2021 ) .
Według ostatniego ukraińskiego spisu ludności z 1 stycznia 2014 roku było to 338 319 osób .
Założone zostało w 1784 roku przez carycę Katarzynę II , rok po przyłączeniu Krymu do Imperium Rosyjskiego , w pobliżu starożytnej stolicy państwa scytyjskiego na Krymie – Neapolu Scytyjskiego .
W XIV wieku istniała tu tatarska osada pod nazwą `` Aqmescit '' .
Symferopol przez długi czas stanowił siedzibę guberni taurydzkiej .
W mieście znajduje się dworzec kolejowy i lotnisko , kursuje trolejbus łączący Symferopol z Jałtą ( to najdłuższa na świecie czynna linia trolejbusu – 86 km ) oraz pociąg do Sewastopola .
W Symferopolu i jego okolicach znajdują się kamieniołomy pozyskujące ozdobne wapienie o dobrym polerze nazywane potocznie marmurami ( choć nie jest to marmur w sensie definicji geologicznej ) .
W mieście znajduje się Krymski Uniwersytet Federalny im .
W. Wiernadskiego .
W obrębie miejscowości ma również swą siedzibę znany rezerwat archeologiczny „ Neapol Scytyjski ” z ruinami stolicy starożytnego państwa scytyjskiego na Krymie .
Polonia .
W Symferopolu działają polskie organizacje „ Stowarzyszenie Polaków na Krymie ” , „ Polski Ośrodek Kulturalno-Oświatowy im .
A. Mickiewicza na Krymie ” i „ Krymski Oddział Stowarzyszenia Uczonych Polskich ” .
