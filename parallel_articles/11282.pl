Symplectromyces vulgaris
Symplectromyces vulgaris ( Thaxt . )
Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Symplectromyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1900 r. Roland Thaxter r. nadając mu nazwę `` Teratomyces vulgaris '' .
W 1908 r. ten sam autor przeniósł go do rodzaju `` Symplectromyces '' .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski opisał jego występowanie na 16 gatunkach chrząszczy z rodzaju marga ( `` Quedius '' ) z rodziny kusakowatych ( Staphylinidae ) .
