Synandromyces telephani
Synandromyces telephani Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniaceae '' ) .
Grzyb entomopatogeniczny ( pasożytujący na owadach ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Synandromyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisany został w 1912 r. przez Rolanda Thaxtera w Buenos Aires na przedtułowiu owada `` Telephanus '' .
W Polsce notowany w 2003 r. na `` Psammococcus bipunctatus '' .
