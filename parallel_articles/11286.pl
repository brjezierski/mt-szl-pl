Senegal
Senegal ( Republika Senegalu , fr .
`` République du Sénégal '' ) – państwo w zachodniej Afryce nad Oceanem Atlantyckim .
Graniczy z Mauretanią , Mali , Gwineą , Gwineą Bissau i Gambią .
Obecnie członek Unii Afrykańskiej i ECOWAS .
Ustrój polityczny .
Senegal jest republiką z prezydentem i 120 członkami zgromadzenia narodowego wybieranymi w powszechnych wyborach na pięcioletnie kadencje .
Obowiązuje system wielopartyjny .
Główne partie : Socjalistyczna Partia Senegalu ( PSS ) oraz Senegalska Partia Demokratyczna ( PDS ) .
Podstawowe zasady ustroju państwa reguluje Konstytucja z 2001 roku .
Głową państwa jest Prezydent Republiki wybierany w wyborach powszechnych na pięcioletnią kadencję .
Ta sama osoba na urząd prezydenta może być wybrana ponownie tylko raz .
Kandydat na urząd Prezydenta Republiki musi być obywatelem Senegalu , mieć ukończone w dniu wyborów 35 lat oraz posiadać pełnię praw obywatelskich i politycznych .
Konstytucja nakłada także obowiązek płynnego pisania , czytania oraz mówienia w oficjalnym języku ( tj .
w języku francuskim ) przez kandydata .
Podstawowe znaczenie i charakter urzędu prezydenta określone zostały w art .
42 konstytucji .
Zgodnie z tym artykułem Prezydent Republiki stoi na straży przestrzegania Konstytucji , ucieleśnia jedność narodową , a także jest gwarantem prawidłowego funkcjonowania instytucji , suwerenności państwa oraz integralności jego terytorium .
