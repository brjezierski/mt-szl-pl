Alternaria raphani
Alternaria raphani J.W .
Groves & amp ; Skolko – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny będący jednym z patogenów wywołujących u roślin chorobę o nazwie czerń krzyżowych .
Na półkuli północnej jest szeroko rozprzestrzeniony .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Alternaria , Pleosporaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Znana jest tylko anamorfa tego gatunku .
Na podstawie budowy molekularnej i ultrastrukturalnej ustalono , że teleomorfą gatunków z rodzaju `` Alternaria '' jest któryś z gatunków grzybów zaliczany do rodzaju `` Lewia '' .
