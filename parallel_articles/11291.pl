Syosset
Syosset – miasteczko w Hrabstwie Nassau ( stan Nowy Jork ( stan ) , USA .
Ludność 18 544 ( 2000 r. ) .
Syosset leżo obok miasta Oyster Bay .
Geografia .
Według danych Amerykańskiego Urzędu Statystycznego , wioska zajmuje powierzchnię 12,9 km² ( 5,0 mi² ) .
Całość zajmują lądy .
Demografia .
Spis ludności z 2000 roku , wykazał 18 544 mieszkańców , 6281 gospodarstw domowych i 5307 rodzin .
