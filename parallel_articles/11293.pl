Serbia
Serbia , Republika Serbii ( , ) – państwo w Europie Południowej , na Półwyspie Bałkańskim , śródlądowe , powstałe 5 czerwca 2006 po rozpadzie federacji Serbii i Czarnogóry .
Powierzchnia Serbii wynosi 88 361 km² ( bez spornego Kosowa 77 474 km² ) .
Serbia była wcześniej republiką związkową Federacyjnej Republiki Jugosławii ( 1992–2003 ) oraz Serbii i Czarnogóry ( 2003–2006 ) .
Stolicą jest Belgrad .
Walutą Serbii jest dinar serbski .
Serbia graniczy z Węgrami ( 151 km ) na północy , Rumunią ( 476 km ) i Bułgarią ( 318 km ) na wschodzie , Macedonią Północną ( 221 km lub 62,3 km nie licząc Kosowa ) i Albanią ( 115 km ) na południu oraz z Czarnogórą ( 221 km lub 124,4 km nie licząc Kosowa ) , Chorwacją ( 241 km ) i Bośnią i Hercegowiną ( 302 km ) na zachodzie .
`` De facto '' na południu Serbia graniczy z Kosowem , którego jednostronnie ogłoszoną niepodległość ( 2008 ) uznaje część państw .
Od 29 lutego 2012 kraj ten posiada status oficjalnego kandydata do Unii Europejskiej , w czerwcu 2013 Rada Europejska wyraziła zgodę na podjęcie rozmów akcesyjnych , które oficjalnie rozpoczęły się w styczniu 2014 .
Serbia dzieli się na 17 okręgów ( Serbia Centralna ) , okręg stołeczny oraz prowincję autonomiczną Wojwodinę , która dzieli się na 7 okręgów .
Ustrój polityczny .
Podstawy prawne ustroju Serbii zawarto w Konstytucji Republiki Serbii , którą zatwierdzono w referendum ( 28–29 października 2006 roku ) .
Prezydent jest wybierany w głosowaniu powszechnym na pięcioletnią kadencję ( z prawem do jednej reelekcji ) .
Władza ustawodawcza należy do Zgromadzenia Narodowego ( Skupsztiny ) , wybieranej na czteroletnią kadencję .
Skupsztina liczy 250 członków .
Władzę wykonawczą sprawuje rząd , z premierem na czele .
Premiera powołuje parlament .
Władza sądownicza należy do niezawisłych sądów .
Obecnym prezydentem Serbii jest Aleksandar Vučić , przewodniczący Serbskiej Partii Postępowej .
Urząd objął 31 maja 2017 roku .
Premierem Serbii jest Ana Brnabić .
