`` Republika Srpska '' ) – jedna z dwóch części składowych Bośni i Hercegowiny ( drugą jest Federacja Bośni i Hercegowiny ) .
Obecny status Republiki Serbskiej uregulowany jest porozumieniem pokojowym , którym zakończyła się wojna domowa w Bośni i Hercegowinie ( 1992–1995 ) .
Ludność .
Republikę Serbską zamieszkuje ok. 1,4 mln osób ( 2007 r. ) ; 90 % tej liczby stanowią Serbowie , oprócz nich żyją tu głównie Chorwaci i Boszniacy ( bośniaccy muzułmanie ) .
