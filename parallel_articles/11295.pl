Język serbski
Język serbski ( serb .
/ ) – język indoeuropejski z grupy zachodniej języków południowosłowiańskich , używany głównie przez Serbów .
Ma status języka urzędowego w Serbii i spornym Kosowie .
W Bośni i Hercegowinie jest jednym z trzech języków urzędowych , obok bośniackiego i chorwackiego .
W sąsiednich i innych pobliskich krajach , m.in .
