Historia .
Pierwsze państwo na terytorium Syrii to Ebla , po raz pierwszy wymieniona w tekście syryjskim datowanym na ok. 2500–2400 p.n.e .
Później była ważną prowincją rzymską , podbitą w roku 64 p.n.e .
przez Pompejusza .
Pozostała pod panowaniem rzymskim ( bizantyjskim ) .
W latach 634–637 Syria została podbita przez Arabów .
W 661 roku Damaszek stał się stolicą kalifatu Umajjadów , stając się centrum kulturalnym świata islamu .
Po przeniesieniu przez Abbasydów stolicy do Bagdadu w 750 roku Syria znalazła się na obrzeżach muzułmańskiego świata .
W 877 Tulunidzi przyłączyli ją do Egiptu .
Na terenie Syrii dynastie egipskie toczyły wojny z Mongołami , krzyżowcami i Bizancjum .
Doprowadziło to do wyludnienia kraju i osłabienia go gospodarczo .
W latach 1400–1401 miał miejsce najazd wojsk Timura na północną Syrię , złupienie i wymordowanie mieszkańców Aleppo , Hamy , Homs i Damaszku .
W 1517 Syrię przyłączono do Imperium Osmańskiego .
W 1831 Syrię zajęły wojska paszy Egiptu Muhammada Alego , jednak na skutek nacisków europejskich mocarstw i buntu miejscowej ludności wojska musiały wycofać się z Syrii na rzecz Turcji w 1840 .
