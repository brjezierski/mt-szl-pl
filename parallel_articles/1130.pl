Altötting ( stacja kolejowa )
Altötting ( niem : `` Bahnhof Altötting '' ) – stacja kolejowa w Altötting , w kraju związkowym Bawaria , w Niemczech .
Znajduje się na linii Mühldorf – Burghausen .
Według DB Station & amp ; Service ma kategorię 6 .
W Altötting zatrzymuje się codziennie około 35 regionalnych pociągów Südostbayernbahn .
Budynek dworca jest obiektem zabytkowym .
