System of a Down
System of a Down , często skracane do SOAD czy System – amerykański zespół wykonujący szeroko pojętą muzykę rockową i metalową .
Grupa została założona w 1994 roku w Glendale w stanie Kalifornia przez muzyków pochodzenia ormiańskiego .
Działalność zespołu została zawieszona w 2006 .
W 2010 roku formacja wznowiła działalność .
Historia .
Na początku lat 90 .
Serj Tankian grał na keyboardzie w zespole , który dzielił salę prób z zespołem , w którym grał Daron Malakian .
Obydwaj chodzili do szkoły Rose and Alex Pilibos , ale znali się tylko ze słyszenia .
Szybko zauważyli , że mają ze sobą dużo wspólnego i razem założyli zespół o nazwie Soil ( 1992–1994 ) , który później zmienił nazwę na System of a Down .
Z biegiem czasu do zespołu doszedł Shavo Odadjian , poprzedni menadżer zespołu , oraz Ontronik Khachaturian .
Po kontuzji dłoni jego miejsce zajął John Dolmayan .
