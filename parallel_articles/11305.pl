Szadź ( serial telewizyjny )
Szadź – polski serial kryminalny udostępniany w serwisie Player od 30 kwietnia 2020 oraz emitowany od 13 października 2020 na antenie telewizji TVN .
Fabułę serialu oparto na powieści autorstwa Igora Brejdyganta o tym samym tytule .
Fabuła .
W lesie niedaleko Opola w bardzo brutalny sposób zostaje zamordowana młoda kobieta , która była jedną z ofiar seryjnego mordercy , Piotra Wolnickiego ( Maciej Stuhr ) .
W śledztwo angażuje się komisarz Agnieszka Polkowska ( Aleksandra Popławska ) .
Kobieta nie wie jednak , że jej obecność w śledztwie nie jest przypadkowa .
Produkcja i odbiór .
Zdjęcia do pierwszego sezonu produkcji rozpoczęły się w październiku 2019 w Opolu , Warszawie i Gołkowie .
Akcja serialu rozgrywa się w Opolu .
Premiera serialu odbyła się 30 kwietnia 2020 na platformie VOD Player .
W czerwcu 2020 zapowiedziano drugi sezon produkcji , do którego zdjęcia rozpoczęły się w grudniu 2020 .
Premierowy odcinek serialu na antenie telewizji TVN obejrzało średnio 1,24 mln widzów , natomiast średnia oglądalność pierwszych trzech odcinków wyniosła 1,21 mln widzów .
Cały sezon w telewizji obejrzało średnio 1,24 mln widzów , co podniosło udziały stacji .
Produkcja została nominowana do nagrody Telekamery 2021 w kategorii „ Serial ” .
Pierwsze trzy odcinki drugiego sezonu serialu w telewizji zostały obejrzane przez średnio 805 tys .
