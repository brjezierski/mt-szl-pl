Szahada
Szahada ( arab .
شهادة , `` šahāda '' ) – muzułmańskie wyznanie wiary , będące jednym z pięciu obowiązków każdego muzułmanina ( w islamie sunnickim ) .
Szyici także wiążą szahadę ze swoimi filarami wiary .
Składa się ono ze słów w języku arabskim :
لَا إِلٰهَ إِلَّا الله مُحَمَّدٌ رَسُولُ الله ( lā ʾilāha ʾillā-llāh , muḥammadun rasūlu-llāh ) ,
tzn .
„ Nie ma boga prócz Allaha , a Mahomet jest jego Prorokiem ” .
W islamie szyickim wyznanie wiary jest czasem uzupełniane o odniesienie do imama Alego :
وعليٌ وليُّ الله ( wa ʿalīyyun walīyyu-llāh ) ,
tzn .
„ a Ali jest `` walim '' Allaha ” .
Świadome , szczere i celowe , publiczne jego wypowiedzenie ( czyli danie świadectwa wiary ) decyduje o przyjęciu islamu przez daną osobę .
Po wypowiedzeniu szahady wszystkie grzechy „ poprzedniego życia ” znikają , a człowiek rodzi się na nowo .
Słowa wyznania wiary znajdują się w każdej modlitwie muzułmańskiej i stanowią podstawowy dogmat wiary .
