Szalonki
Szalonki – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie radziejowskim , w gminie Osięciny .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa włocławskiego .
Wieś sołecka – zobacz jednostki pomocnicze gminy w BIP .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 135 mieszkańców .
Jest 22. co do wielkości miejscowością gminy Osięciny .
