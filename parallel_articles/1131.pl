Alwernia
Alwernia – miasto w południowej Polsce , w województwie małopolskim , w powiecie chrzanowskim , siedziba władz gminy miejsko-wiejskiej Alwernia .
Według danych GUS z 31 grudnia 2019 r. miasto miało 3344 mieszkańców .
Położenie .
Miasto położone jest na Wyżynie Krakowsko-Częstochowskiej , nad rzeką Regulką .
Gmina jest zaliczana do ośrodków miejskich aglomeracji krakowskiej .
Alwernia należy do Jaworznicko-Chrzanowskiego Okręgu Przemysłowego .
