Szanghaj
Szanghaj ( `` '' , chiń .
: 上海 ; pinyin : `` Shànghǎi '' ) – miasto we wschodnich Chinach , przy ujściu rzeki Jangcy , jedno z czterech miast wydzielonych Chińskiej Republiki Ludowej .
Cała jednostka administracyjna liczyła w 2018 roku 24 237 800 mieszkańców .
Port w Szanghaju jest największym na świecie pod względem wielkości przeładunku kontenerowego .
Działa tutaj również jedna z największych giełd .
W dzielnicy Pudong , leżącej naprzeciwko historycznego Bundu , skupia się jedna z największych na świecie liczba wieżowców .
W mieście znajduje się Konsulat Generalny RP .
Nazwa .
Nazwa miasta składa się ze znaków „ na ” i „ morze ” ( nie znaczy jednak „ na morzu ” – „ na morzu ” to po chińsku 海上 hǎi shàng ) , często interpretuje się ją jako „ Najdalszy zasięg morza ” lub „ ( Naprzód ) w morze ” .
Skrócone nazwy to 滬 ( uproszcz .
沪 , Hù ) lub 申 ( Shēn ) .
Szanghaj miał także liczne przydomki , nadane przez przybyszów z zachodu , m.in .
`` Paryż Wschodu '' , `` Królowa Orientu '' i `` Azjatycka Prostytutka '' ( chodzi tu o lata upadku Szanghaju – dwudzieste i trzydzieste – kiedy szerzyła się przestępczość i prostytucja ) .
Geografia i klimat .
Szanghaj leży przy ujściu rzeki Jangcy do Morza Wschodniochińskiego ( część Oceanu Spokojnego ) , zaś od południa nad zatoką Hangzhou .
Przy wschodniej granicy miasta znajduje się jezioro Dian Shan o powierzchni 62 km² .
Miasto , które jest przedzielone na dwie części rzeką Huangpu , leży na równinie , której średnia wysokość nad poziomem morza wynosi około czterech metrów , zaś najwyższym punktem jest wzgórze Dajin , które ma 103,4 m wysokości .
Ponadto powiat Chongming obejmuje trzy wyspy położone w ujściu rzeki Jangcy – Chongming ( pow .
1267 km² ) , Changxing ( pow .
88 km² ) i Hengsha ( pow .
56 km² ) .
W Szanghaju występują wszystkie cztery pory roku .
Latem temperatury osiągają 37 °C , a niekiedy przekraczają 40 °C .
