Szarvas
Szarvas ( niem .
`` Sarwasch '' , słow .
`` Sarvaš '' ) – miasto w południowo-wschodnich Węgrzech , w komitacie Békés , nad Kereszem ( dopłwy Cisy ) .
Liczy prawie 17,2 tys .
mieszkańców ( I 2011 ) .
Nazwa miasta oznacza po polsku `` jeleń '' .
Historia .
W Szarvas została założona przez słowackiego oświeconego profesora i inżyniera Samuela Tešedíka ( węg .
Sámuel Tessedik ) pierwsza wyższa szkoła rolnicza w Królestwie Węgier .
Miasto było odnowione przez Słowaków w XVIII wieku .
Wielu z nich nadal tu mieszka .
Niedaleko miasta znajduje się geometryczny środek Królestwa Węgier ( sprzed roku 1920 ) .
