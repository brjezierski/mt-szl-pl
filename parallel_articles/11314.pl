Aktor
Aktor ( z , od ’ działać ’ ) – osoba odgrywająca za pomocą szczególnego zachowania , technik cielesnych i głosowych rolę w serialu , w teatrze i w filmie .
Od zwykłego człowieka odróżnia go ze strony samego aktora świadomość gry , ze strony widzów – szczególny sposób traktowania osoby jako aktora .
Wobec widza istnieje na sposób dwoisty , jako odtwarzana postać , w którą się tymczasowo wciela , i zarazem jako żywy człowiek , posiadający własną rzeczywistą tożsamość i biografię .
Historia .
W czasach starożytnych , w 534 p.n.e. , grecki poeta , twórca tragedii i aktor Tespis wprowadził w swej sztuce , obok chóru , pierwszego aktora .
Wystawienie jej odbyło się podczas Wielkich Dionizjów ( prawdopodobnie 23 października ) i tym samym stał się on pierwszym , który wypowiedział tekst nie w trzeciej osobie , ale jako postać z przedstawienia .
Zrewolucjonizowało to sposób prezentowania opowieści : przedtem używano chórów , narratora , nie odgrywano jednak roli postaci .
Pomiędzy aktorem a chórem lub aktorem a koryfeuszem zaistniała możliwość prowadzenia dialogu , mógł się więc pojawić konflikt dramatyczny .
Drugiego aktora wprowadził Ajschylos , a trzeciego – Sofokles .
Sztuka aktorska .
Od aktorów oczekuje się zwykle całego zestawu umiejętności , m.in .
dobrej emisji głosu , dykcji , wyrazistości ruchu i mimiki , zdolności analizowania i rozumienia tekstu dramatycznego , zdolności wywoływania i odgrywania uczuć .
Obecnie nie przykłada się specjalnej wagi do cech fizycznych , przestały obowiązywać kanony piękna ( podlegające zresztą przemijającej modzie ) .
Zwykle dobrzy aktorzy są wyszkoleni w śpiewie , tańcu , improwizacji , obserwacji i naśladowaniu , walce scenicznej i odgrywaniu tekstów klasycznych , posługiwaniu się dialektem i gwarą .
Wielu aktorów bierze udział w długotrwałych kursach i uczęszcza do szkół pozwalających rozwinąć takie umiejętności .
Szkoły te mają często bardzo różne filozofie i metodologie artystyczne .
Pionierzy nowoczesnej sztuki aktorskiej to m.in .
Konstanty Stanisławski , Lee Strasberg , Uta Hagen , Stella Adler i Sanford Meisner .
W Polsce od 1961 odbywają się Kaliskie Spotkania Teatralne – Festiwal Sztuki Aktorskiej , konkurs aktorski organizowany przez Teatr im .
Wojciecha Bogusławskiego w Kaliszu .
Aktorstwo a płeć .
W przeszłości pojęcie aktora ograniczało się jedynie do mężczyzn .
W starożytności i średniowieczu uważano , że występowanie na scenie upadla kobietę .
Dopiero w XVII w. zaprzeczyli temu Wenecjanie i kobiety zaczęły powszechnie brać udział w przedstawieniach .
W czasach Szekspira role kobiet odgrywali mężczyźni i chłopcy , chociaż istnieją dowody na potajemne występowanie kobiet przebranych za mężczyzn .
Aktorki czasem występują w rolach młodych chłopców , ponieważ pod pewnymi względami chłopiec bardziej jest podobny do dorosłej kobiety niż dorosłego mężczyzny .
W angielskich teatrach rola „ Piotrusia Pana ” jest tradycyjnie odtwarzana przez kobietę .
Dorosły w roli dziecka występuje częściej w spektaklu niż w filmie .
Wyjątkiem jest dubbing w filmach animowanych , gdzie głosy chłopców są zwykle zarezerwowane dla kobiet .
W Wenecji długą tradycję ma odgrywanie płci przeciwnej dla efektu komicznego .
Większość komedii Szekspira zawiera elementy przebierania się .
W filmowych rolach wymagających gry w roli kobiety wystąpili m.in .
Wojciech Pokora ( `` Poszukiwany , poszukiwana '' ) , Dustin Hoffman ( `` Tootsie '' ) i Robin Williams ( `` Pani Doubtfire '' ) .
