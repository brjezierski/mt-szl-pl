Szczawin ( powiat płoński )
Szczawin – wieś w Polsce położona w województwie mazowieckim , w powiecie płońskim , w gminie Nowe Miasto .
Prywatna wieś szlachecka `` Szczawino '' położona była w drugiej połowie XVI wieku w powiecie nowomiejskim ziemi zakroczymskiej województwa mazowieckiego .
W latach 1975–1998 miejscowość należała administracyjnie do województwa ciechanowskiego .
