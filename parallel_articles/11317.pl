Szczawnica
Szczawnica – miasto uzdrowiskowe w województwie małopolskim , w powiecie nowotarskim , siedziba władz gminy miejsko-wiejskiej Szczawnica .
Położone jest nad potokiem Grajcarkiem , na pograniczu Pienin i Beskidu Sądeckiego .
Obszar Szczawnicy zajmuje 32,9 km² , w 2022 r. zamieszkiwało ją 5369 osób .
Pierwsze wzmianki o Szczawnicy pochodzą z 1413 r. Za twórcę uzdrowiska uznawany jest Józef Szalay , który przejął miejscowość w 1839 r. , wzniósł pierwsze pensjonaty i restauracje oraz urządził Park Górny .
W 1876 r. przeszło ono w ręce Akademii Umiejętności , która sprzedała je w 1909 r. hrabiemu Adamowi Stadnickiemu .
Nowy właściciel wyremontował domy zdrojowe oraz wybudował sanatoria : „ Inhalatorium ” oraz „ Modrzewie ” .
Po II wojnie światowej w 1948 r. uzdrowisko upaństwowiono , a w drugiej połowie XX w. Szczawnica przeżyła okres dynamicznego rozwoju .
22 lipca 1962 r. miejscowości nadano prawa miejskie .
