Szczebrzeszyn
Szczebrzeszyn – miasto w Polsce w województwie lubelskim , w powiecie zamojskim , siedziba gminy Szczebrzeszyn , nad rzeką Wieprz .
Według danych GUS z 31 grudnia 2019 r. Szczebrzeszyn liczył 4964 mieszkańców .
Przez Miasto przebiega droga krajowa nr 74
Położony jest w historycznej ziemi chełmskiej .
Niegdyś gród obronny na trakcie kijowskim .
Prywatne miasto szlacheckie lokowane w 1352 roku położone było w XVI wieku w województwie ruskim .
W latach 1975–1998 miasto administracyjnie należało do województwa zamojskiego .
Szczebrzeszyn posiada prawa miejskie od 1352 roku ( nadane przez Kazimierza Wielkiego ) ; w czasie reformacji miasto było ośrodkiem kalwińskim .
