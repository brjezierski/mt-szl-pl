Szczeglacin
Szczeglacin – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Korczew .
Przybliżone odległości : Korczew - 3 km , Siedlce - 30 km , Warszawa - 109 km .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Na terenie wsi znajduje się staw wędkarski oraz strażnica Ochotniczej Straży Pożarnej .
Od zachodu wieś graniczy z Rezerwatem przyrody Kaliniak .
Na wschód od wsi płynie rzeka Kołodziejka .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Stanisława Biskupa Męczennika i św .
Anny w Knychówku .
