Szczepan Twardoch
Szczepan Lech Twardoch ( ur .
23 grudnia 1979 w Knurowie ) – polskojęzyczny prozaik i publicysta .
Okazjonalnie pisze też po śląsku .
Identyfikuje się jako przedstawiciel narodowości śląskiej .
Życiorys .
Ukończył socjologię na Międzywydziałowych Indywidualnych Studiach Humanistycznych na Uniwersytecie Śląskim w Katowicach .
Publikował w „ Życiu ” , „ Opcjach ” , „ Frondzie ” , „ 44/Czterdzieści i Cztery ” , „ Nowej Gazecie Śląskiej ” , „ Arcanach ” , „ Gazecie Wyborczej ” i „ Rzeczpospolitej ” jako niezależny publicysta .
Był redaktorem działu literackiego dwumiesięcznika „ Christianitas ” .
Felietonista „ Polityki ” oraz „ Wysokich Obcasów Extra ” .
Jego opowiadanie `` Obłęd rotmistrza von Egern '' nominowane było do nagrody Nautilus za rok 2003 i zajęło w głosowaniu 4. miejsce .
Opowiadanie pod tytułem `` Rondo '' zdobyło Nautilusa za najlepsze opowiadanie roku 2006 .
Za `` Epifanię wikarego Trzaski '' otrzymał zaś Srebrne Wyróżnienie Nagrody Literackiej im .
Jerzego Żuławskiego w 2008 .
W 2010 ukazał się francuski przekład jego powieści `` Przemienienie '' ( `` Transfiguration '' ) .
W 2011 roku otrzymał za powieść `` Wieczny Grunwald '' Wyróżnienie Nagrody im .
Józefa Mackiewicza .
Za zbiór opowiadań `` Tak jest dobrze '' został nominowany do Nagrody Literackiej Gdynia 2012 .
W 2013 został laureatem Paszportu Polityki za rok 2012 w kategorii literatura .
Nagrodę otrzymał za powieść `` Morfina '' – powieść ta była w finale Nagrody Literackiej Nike 2013 oraz otrzymała nominację do Nagrody Literackiej Gdynia 2013 i nominację do Prix du Livre Européen / European Book Prize 2015 .
W 2014 wyszła kolejna jego książka – `` Drach '' , która znalazła się w finale Nagrody Literackiej Nike 2015 i zdobyła niemiecką nagrodę literacką Brücke Berlin 2016 .
W 2015 został laureatem Nagrody Fundacji im .
Kościelskich .
W kwietniu 2017 otrzymał główną nagrodę w plebiscycie „ O ! Lśnienia 2016 ” w kategorii „ Literatura ” , organizowanym przez Onet.pl .
W 2017 był nominowany do Nagrody Literackiej Gdynia w kategorii proza za powieść `` Król '' .
W 2019 został laureatem Nagrody im .
Samuela Bogumiła Lindego .
Zdjęcia do serialu `` Król '' produkowanego dla Canal+ opartego na powieści Twardocha zostały zakończone w październiku 2019 , gdzie pisarz wystąpił gościnnie , wcielając się w postać kierownika sali w restauracji .
W lutym 2022 roku Szczepan Twardoch otrzymał Nagrodę im .
Kazimierza Kutza .
Na podstawie twórczości Twardocha powstały spektakle : `` Król '' ( premiera w 2018 , reżyseria Monika Strzępka ) , `` Drach '' ( 2018 , Robert Talarczyk ) , `` Pokora '' ( 2021 , Robert Talarczyk ) i `` Byk '' ( 2022 , Robert Talarczyk i Szczepan Twardoch ) .
Szczepan Twardoch jest Ślązakiem , mieszka w Pilchowicach .
Jego żoną jest architektka i urbanistka Agata Twardoch .
