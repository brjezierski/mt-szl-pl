Kolonia Szczerbacka
Kolonia Szczerbacka – wieś w Polsce położona w województwie mazowieckim , w powiecie przysuskim , w gminie Przysucha .
W latach 1975–1998 miejscowość administracyjnie należała do województwa radomskiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Teresy od Dzieciątka Jezus w Ruskim Brodzie .
