Szczodrochowo ( powiat wągrowiecki )
Szczodrochowo – wieś w Polsce położona w województwie wielkopolskim , w powiecie wągrowieckim , w gminie Skoki .
Historia .
W 1846 wieś wchodziła w skład dóbr Stawiany , należących do Ferdynanda Kalksteina ( we wsi stało wówczas 15 domów i zamieszkiwało 65 mieszkańców ) .
Parcelacja majątku miała miejsce już w okresie zaboru pruskiego .
W Szczodrochowie powstały duże gospodarstwa niemieckie i domostwa pracowników rolnych .
