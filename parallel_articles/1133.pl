Znacząca część ludności alzackiej posługuje się płynnie miejscowym językiem – alzackim – który jest dialektem języka niemieckiego z pewnymi zapożyczeniami z francuskiego .
Przez długi czas był on językiem rodzimym dla większości Alzatczyków .
W okresach 1871–1919 oraz 1940–44 , gdy tereny te podlegały władzy Niemiec , językiem urzędowym był niemiecki .
Mimo to w niektórych górskich dolinach Wogezów , nawet w tych okresach , istniały osady , w których ludność posługiwała się wyłącznie językiem francuskim .
