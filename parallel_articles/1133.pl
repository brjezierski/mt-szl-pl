Alzacja
Alzacja ( , al .
`` Elsàss '' , ) – kraina historyczna w północno-wschodniej Francji , nad Renem , w regionie administracyjnym Grand Est , w departamentach Dolny Ren na północy i Górny Ren na południu .
Historyczna Alzacja obejmowała jeszcze niewielki obszar wokół miasta Belfort , który obecnie tworzy departament Territoire de Belfort .
Od 1982 roku do 31 grudnia 2015 roku odrębny region administracyjny .
Geografia .
Region Alzacji ma kształt wydłużony z północy na południe .
Graniczy od północy i wschodu z Niemcami , od południa ze Szwajcarią i regionem Franche-Comté , a od zachodu z Lotaryngią .
Położony jest na równinie nadrzecznej , ograniczonej od wschodu Renem , a od zachodu masywem Wogezów .
Do głównej doliny schodzą z zachodu na wschód doliny mniejszych dopływów Renu .
Znajdują się tu rozległe tereny leśne , głównie w Wogezach oraz na północy departamenty Bas-Rhin ( lasy Haguenau ) .
