Ludność .
Alzacja , zajmująca powierzchnię 8280 km² , do stycznia 2016 była najmniejszym regionem administracyjnym Francji metropolitalnej .
Jednocześnie należy do najgęściej zaludnionych – na 1 km² przypada średnio 217 osób .
