Flaga .
Historyczna flaga Alzacji składa się z dwóch części ( pasm ) : pas czerwony na górze i biały na dole .
Odnosi się ona do XIX w. , kiedy to kolory biały i czerwony często występowały w herbach miast i rodów alzackich .
25 czerwca 1912 roku parlament Elsaß-Lothringen uznał tę flagę za symbol regionalny .
Herb .
Herb Alzacji jest połączeniem herbu departamentu Bas-Rhin ( pierwsza część ) i herbu departamentu Haut-Rhin ( druga część ) .
