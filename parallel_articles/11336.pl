Grzechotnik teksaski
Grzechotnik teksaski ( `` Crotalus atrox '' ) – północnoamerykański przedstawiciel podrodziny grzechotnikowatych z rodziny żmijowatych .
Duży ( do 2,2 m długości ) , masywny , jest najniebezpieczniejszym grzechotnikiem Ameryki Północnej .
Zamieszkuje południe USA i północną część Meksyku .
Poluje przede wszystkim na małe ssaki i ptaki , ale ze względu na toksyczność jadu jego ugryzienie może być śmiertelne nawet dla człowieka – co roku notuje się w USA kilka śmiertelnych przypadków , aczkolwiek istnieje skuteczne antidotum w pełni zabezpieczające przed skutkami pokąsania .
Grzechotka na końcu ogona to narząd ostrzegawczy .
Węże przed pierwszą wylinką jej nie mają , u dorosłych składa się z ok. 10 pustych komór , które szeleszczą trąc o siebie .
Po każdej nowej wylince przybywa jedna komora grzechotki .
Wąż ten ma najgłośniejszą grzechotkę ze wszystkich grzechotników .
Czasami cała grzechotka może się oderwać i zaczyna rosnąć od nowa .
Dużą grzechotkę słychać z kilkudziesięciu metrów .
Przy grzechotce występują czarno białe paski .
Niektórzy nazywają go najgroźniejszym wężem północnoamerykańskim .
Zajmuje pierwsze miejsce w statystyce , jeśli chodzi zarówno o osoby pokąsane , jak i wypadki śmiertelne .
Zaraz po grzechotniku diamentowym jest największym jadowitym wężem Ameryki Północnej .
Ponieważ ma on poza człowiekiem tylko nielicznych wrogów , jest liczny na całym swoim rozległym obszarze występowania .
