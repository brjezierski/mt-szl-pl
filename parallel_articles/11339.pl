Szczytno ( województwo wielkopolskie )
Szczytno – wieś w Polsce położona w województwie wielkopolskim , w powiecie obornickim , w gminie Rogoźno .
Wieś Sczitno położona była w 1580 roku w powiecie poznańskim województwa poznańskiego .
W latach 1975–1998 miejscowość należała administracyjnie do województwa pilskiego .
