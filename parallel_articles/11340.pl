Segedyn
Segedyn ( wymowa [ ] , , ) – trzecie pod względem liczby ludności miasto Węgier , położone nad Cisą na południu kraju , na wschód od Dunaju .
Stolica komitatu Csongrád .
1 stycznia 2017 liczba mieszkańców Segedynu wyniosła 161 tys .
Położenie geograficzne .
Segedyn jest położony niedaleko południowej granicy Węgier , na południe od ujścia rzeki Maruszy do rzeki Cisy .
Miasto w 2011 miało 170 285 mieszkańców , przy gęstości zaludnienia 606,0 osoby na 1 km² .
Segedyn określa się też często jako `` Miasto słońca '' , bowiem ma bardzo dużo słonecznych godzin w roku w porównaniu z innymi węgierskimi miastami .
Historia .
Obszar , na którym znajduje się dziś miasto Segedyn , był zamieszkany już w czasach starożytnych , a Ptolemeusz wymienia najstarszą nazwę miasta – Partiscum .
Możliwe , że w tych okolicach mieściła się też siedziba władcy Hunów Attyli .
Obecna węgierska nazwa miasta – `` Szeged '' – pojawiła się w 1138 w dokumencie króla Beli III .
