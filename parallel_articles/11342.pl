Szeki
Szeki ( ) – miasto w północnym Azerbejdżanie .
Jest jednym z miast wydzielonych i siedzibą administracyjną rejonu Szeki .
Miasto położone jest na skraju wyżyny Xan , niedaleko od południowych stoków Wielkiego Kaukazu .
Miasto opływają Qurcanaçay i Dəyirmanarxı .
Historia .
Nazwa Szeki wywodzona jest od ludu Saków , zwanego także Scytami ałtajskimi – koczowniczego plemienia , które prawdopodobnie przybyło w te strony w VII wieku p.n.e .
i zamieszkiwało te okolice przez kilkaset lat .
Przedstawiciele tego plemienia w I wieku p.n.e .
walczyli przeciw Rzymianom i niedaleko od miasta pokonali Pompejusza .
Już wcześniej miasto było jednym z ośrodków Albanii Kaukaskiej i jej centrum religijnym .
Wówczas miasto rozciągało się wyżej w górach , w miejscu gdzie obecnie znajduje się wieś Kiş .
