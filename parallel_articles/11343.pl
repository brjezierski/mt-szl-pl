Czekolada
Czekolada – wyrób cukierniczy sporządzany z miazgi kakaowej , tłuszczu kakaowego ( masło kakaowe ) lub innego tłuszczu roślinnego , środka słodzącego i innych dodatków , a w przypadku czekolady mlecznej także z mleka .
Według dyrektywy 2000/36/WE czekoladę definiuje się jako wyrób otrzymywany z wyrobów kakaowych i cukrów , który zawiera nie mniej niż 35 % suchej masy kakaowej ogółem , w tym nie mniej niż 14 % suchej odtłuszczonej masy kakaowej .
W celu uzyskania miazgi kakaowej i tłuszczu kakaowego , ziarna kakaowca poddaje się fermentacji , suszy i rozciera .
Kolejnym etapem produkcji jest konszowanie .
Światowy Dzień Gorzkiej Czekolady ( Goryczki ) obchodzony jest 7 lipca .
W Polsce świętowany jest Dzień Czekolady wypadający 12 kwietnia .
Historia .
Majowie i Aztekowie .
Nasiona kakaowca właściwego początkowo były wykorzystywane przez Majów do produkcji napoju dla najbogatszych o nazwie Xococalit ( z nahuatl `` xocolatl '' IPA [ ʃoˈkola : t͡ɬ ] – gorzka woda ) .
Napój ten był również wykorzystywany podczas modłów i odpraw rytualnych .
Xococalit był przyrządzany z roztartych ziaren kakaowca z miodem , chili bądź z kukurydzą .
W celu uzyskania pianki przelewali napój z naczynia do naczynia .
Aztekowie rozdrabniali nasiona kakaowca by produkować zimny napój z kukurydzą , chili , miodem , wanilią oraz z suszonymi płatkami kwiatów nadających różną barwę napojom .
Europa ( XVII w. – obecnie ) .
Dzięki odkryciu Ameryki przez Krzysztofa Kolumba nasiona kakaowca mogły zostać przewiezione do Europy .
Nie zachwyciły jednak dworu hiszpańskiego , ponieważ Europejczycy nie wiedzieli jak je wykorzystać .
Uwagę na nasiona kakaowca zwrócił Hernán Cortés , gdy w 1518 roku odkrył silos zawierający 100 milionów ziaren kakaowca , posortowanych w 40 tys .
