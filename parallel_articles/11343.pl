Jednak dopiero w XVII wieku popularne stały się roztarte ziarna kakaowca , które zostały zalane ciepłą wodą z dodatkiem cukru .
W 1828 Casparus van Houten Senior opatentował metodę proszkowania nasion kakaowca a jego syn wynalazł proces alkalizowania czekolady zwany `` dutchingiem '' .
Francis Fry z połączonego kakao , cukru i rozpuszczonego tłuszczu kakaowego wylanego do formy uzyskał pierwszą tabliczkę czekolady .
Konszowanie czekolady zostało wynalezione przez Rudolphe ’ a Lindta .
Rodzaje czekolady .
W produktach czekoladopodobnych zawartość kakao nie przekracza 7 % całkowitej masy .
Większość producentów dla uzyskania gładkiej , jednorodnej struktury dodaje do czekolady lecytynę sojową ( E322 ) , lub PGPR ( polirycynooleinian poliglicerolu – E476 ) , co pozwala zmniejszyć zawartość drogiego masła kakaowego i obniżyć lepkość masy .
Składniki odżywcze .
Kakao i czekolada są zaliczane do produktów wysokokalorycznych , 100 g czekolady gorzkiej 99 % zawierać może np .
: 530 kcal , 49,0 g tłuszczu , 13,0 g białka , 8,0 g węglowodanów , a w nich 6,0 g błonnika .
Gorzka czekolada 99 % jest produktem o stosunkowo niskim indeksie glikemicznym ( 20 ) i ładunku glikemicznym ( 1 na 30 gramów produktu ) , dzięki czemu .
Średnio 100 g czekolady mlecznej dostarcza 10 mg cholesterolu , zaś gorzka – 1 mg. Obecne w czekoladzie flawonoidy z rodziny polifenoli mogą hamować utlenianie cholesterolu LDL .
Możliwe jest także , że tłuszcze zawarte w kakao i wysokiej jakości czekoladzie ( nie zawierającej kwasów tłuszczowych o konfiguracji trans oraz sztucznych kwasów krótko-łańcuchowych ) chronią przez nowotworem piersi .
Czekolada zawiera również fenyloetyloaminy ( PEA ) – endorfiny .
Ponadto czekolada zawiera wiele wartościowych składników mineralnych takich , jak Mg , K , Ca , Mn , Cu , Zn , Fe .
Wpływ na organizm .
Wywołuje odczucie sytości i pomaga otrzymać lepsze rezultaty w testach wytrzymałościowych .
Czekolada powoduje wydzielanie endorfin , produkowanych w mózgu i rdzeniu kręgowym , poprawiających nastrój i łagodzących ból .
Zbyt duże ilości spożywanej czekolady mogą prowadzić do otyłości .
Dzienna dawka czekolady powinna wynosić maksymalnie 50 g .
Stwierdzono , że spożywanie czekolady w ilości do 100 g na tydzień może być związane z obniżeniem ryzyka wystąpienia chorób układu krążenia .
Czekolada jest szkodliwa dla wielu zwierząt .
Organizmy koni , psów , kotów i papug nie są zdolne do metabolizmu zawartego w czekoladzie alkaloidu – teobrominy .
Spożycie czekolady może powodować u nich drgawki , zawał serca , krwotok wewnętrzny , a nawet śmierć .
