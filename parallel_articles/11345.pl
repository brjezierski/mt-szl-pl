Szerzyny
Szerzyny – wieś w Polsce , położona w województwie małopolskim , w powiecie tarnowskim .
Miejscowość jest siedzibą gminy Szerzyny .
Pod względem geograficznym znajduje się na Pogórzu Ciężkowickim u południowych podnóży Pasma Brzanki .
Przez miejscowość przepływa rzeka Olszynka .
