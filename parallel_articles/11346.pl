Szernie
Szernie ( dodatkowa nazwa w języku białoruskim Шэрні ) – wieś w Polsce położona w województwie podlaskim , w powiecie bielskim , w gminie Orla .
W latach 1975–1998 miejscowość administracyjnie należała do województwa białostockiego .
W Szerniach urodził się ks .
Grzegorz Sosna .
Prawosławni mieszkańcy wsi należą do parafii św .
Michała Archanioła w Orli , a wierni kościoła rzymskokatolickiego do parafii Najświętszej Opatrzności Bożej w Bielsku Podlaskim .
