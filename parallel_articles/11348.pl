Statek wodny
Statek ( wodny ) – jednostka pływająca o własnym napędzie , przystosowana do przewozu ludzi i towarów .
Pod pojęciem tym zwykle rozumie się konstrukcje o większych gabarytach , zdolne do pokonywania większych akwenów , często ( choć niekoniecznie ) otwartego morza , w odróżnieniu od łodzi .
Większe statki nazywane są okrętami .
Statki wykorzystywane w celach wojskowych nazywane są , niezależnie od wielkości , okrętami wojennymi lub krótko okrętami .
Historia słowa .
Udokumentowane użycie słowa „ statek ” w polszczyźnie ok. 1612 , 1625 .
