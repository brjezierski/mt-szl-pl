Posługiwano się w niej językiem hebrajskim , jidysz i rosyjskim , a Szymon uczył się polskiego w szkole .
Peres władał również językiem angielskim i francuskim .
Jego ojciec był kupcem drzewnym , a matka – bibliotekarką .
Miał młodszego brata , Gerszona .
