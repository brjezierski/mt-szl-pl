Szimon Peres urodził się jako Szymon Perski w 1923 w Wiszniewie , ówcześnie leżącym w województwie nowogródzkim w Polsce ( obecnie obwód miński na Białorusi ) , w rodzinie Icchaka Perskiego ( 1896–1962 ) i Sary Perskiej ( do 1905 Meltzer ) .
