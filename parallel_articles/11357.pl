i ) – kraj stanowiący część Zjednoczonego Królestwa Wielkiej Brytanii i Irlandii Północnej .
W przeszłości był niezależnym królestwem .
Obejmuje północną część wyspy Wielkiej Brytanii oraz archipelagi : Hebrydy , Orkady i Szetlandy .
Na południu graniczy z Anglią .
Krajobraz w przeważającej części wyżynny i górski .
