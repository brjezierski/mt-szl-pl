Liwki Szlacheckie
Liwki Szlacheckie – wieś w Polsce położona w województwie mazowieckim , w powiecie łosickim , w gminie Huszlew .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Antoniego Padewskiego w Huszlewie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
