Alès
Alès – miejscowość i gmina we Francji , w regionie Oksytania , w departamencie Gard .
Miasto jest uważane za „ stolicę ” Sewennów .
Historyczna nazwa miasta to `` Alais '' , natomiast jego mieszkańcy są nazywani `` les Alésiens '' .
