Stobiecko Szlacheckie
Stobiecko Szlacheckie – wieś w Polsce położona w województwie łódzkim , w powiecie radomszczańskim , w gminie Ładzice .
W latach 1975–1998 miejscowość administracyjnie należała do województwa piotrkowskiego .
Historia .
Według „ Słownika geograficznego Królestwa Polskiego ” wieś i folwark Stobiecko Szlacheckie w roku 1827 posiadały 12 domów , 42 mieszkańców i młyn a w roku 1870 folwark miał 888 mórg , 8 budynków murowanych , 11 drewnianych , park z sadem pow .
15 mórg .
W roku 1557 stanowił własność Jana Stobieckiego , w końcu XIX wieku dobra należały do Romualda Hubego .
Na podstawie Ksiąg Hipotecznych znajdujących się w Biurze Notarialnym w Radomsku rep.218 dz .
I i II dobra w roku 1890 miały 733 mrg .
