Bita śmietana
Bita śmietana – śmietana bądź śmietanka , zawierająca zazwyczaj więcej niż 30 % tłuszczu , napowietrzona .
Technicznie rzecz ujmując , bita śmietanka jest jednocześnie emulsją i pianą .
Powietrze wtłaczane podczas ubijania zostaje uwięzione pod postacią pęcherzyków z błonami białkowymi , otoczonych zemulgowanym tłuszczem .
Przepis na bitą śmietanę pojawia się w pierwszej polskiej książce kucharskiej , `` Compendium ferculorum , albo Zebranie potraw '' Stanisława Czernieckiego , wydanej w 1682 roku .
W literaturze gastronomicznej występuje pojęcie kremu bita śmietanka ( lub kremu z bitej śmietany ) .
Powstaje on poprzez krótkie ubicie schłodzonej śmietanki ( 30 % lub 36 % ) dodając pod koniec ubijania cukier puder i wanilinę ( lub etylowanilinę ) .
Krem bita śmietanka zaliczany jest do kremów do tortów z grupy produkowanych na zimno .
Jest to wyrób nietrwały , przeznaczony do konsumpcji w ciągu 6 godzin ( gdy przechowywany w temp .
ok. 6°C ) .
W celu utrwalenia kremu , pod koniec ubijania obok wymienionych składników dodaje się rozpuszczoną żelatynę .
Dawniej używano do tego celu karuku .
Mocno słodka bita śmietanka nazywana jest `` crème chantilly '' .
Regulacje .
Decyzją Trybunału Sprawiedliwości Unii Europejskiej z 2017 roku termin `` bita śmietana '' ( jako `` chantilly '' ) co do zasady zarezerwowany jest wyłącznie dla przetworów mleka zwierzęcego , podobnie jest w wypadku pojęć : ser , śmietana , śmietanka , masło i jogurt .
