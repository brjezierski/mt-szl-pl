Proca neurobalistyczna
Proca katapulta lub proca neurobalistyczna to rodzaj broni miotającej .
Najprostsza ręczna proca katapulta zbudowana jest z patyka w kształcie litery „ Y ” oraz z kawałka gumy przymocowanego do górnych jego końców .
Aby strzelić , najpierw należy naciągnąć gumę z pociskiem ( np .
kamieniem ) , a następnie ją puścić .
Spotykane w handlu proce są wykonane z metalu , bądź materiałów kompozytowych , często wyposażone w celowniki , a także ergonomiczne uchwyty z opieraną o przedramię przeciwwagą .
Amunicją do tych proc są stalowe kulki .
Często używana przez dzieci jako zabawka .
Różnego rodzaju specjalistyczne proce wykorzystywane są także w wędkarstwie do wyrzucania zanęty .
Nie należy mylić procy katapulty z procą – historyczną bronią miotającą .
