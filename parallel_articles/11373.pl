Szoham
Szoham ( hebr .
שוהם ; pol .
`` Onyks '' ; oficjalna pisownia w ang .
Shoham ) – samorząd lokalny położony w Dystrykcie Centralnym , w Izraelu .
Położenie .
Leży w Szefeli w otoczeniu moszawów Bet Arif , Tirat Jehuda , Bareket , Bet Nechemja i Kefar Truman .
Na zachód od miasteczka znajduje się międzynarodowe lotnisko im .
Ben-Guriona , a na południe znajduje się baza logistyczna `` Tovala '' Sił Obronnych Izraela .
