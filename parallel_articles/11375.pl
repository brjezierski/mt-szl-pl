Szombathely
Szombathely ( , , , ) – miasto na zachodzie Węgier , nieopodal granicy z Austrią , siedziba władz komitatu Vas oraz powiatu Szombathely .
Zajmuje powierzchnię 97,52 km² i w 2018 było zamieszkiwane przez 77 984 osoby ( 10. miejsce pod względem liczby mieszkańców na Węgrzech ) .
Historia .
W czasach starożytnych miejsce to stanowiło punkt na traktach handlowych .
Przebiegała tędy jedna z nitek szlaku bursztynowego .
Czynnik ten zadecydował o założeniu w czasach cesarza Klaudiusza osiedla pod nazwą Colonia Claudia Savaria .
W 107 r. stało się ono stolicą świeżo powołanej prowincji Panonia Górna .
W 456 r. Savaria padła ofiarą trzęsienia ziemi .
Przypuszczalnie od XI w. miasto podporządkowane było biskupstwu Győr .
Reforma administracji kościelnej wyniosła w 1777 r. Szombathely do rangi stolicy nowo utworzonego biskupstwa .
W II połowie XIX w. miasto zostało ważnym węzłem kolejowym , w którym zbiegały się linie z Wiednia do Zagrzebia i z Budapesztu do Grazu .
Miasto poniosło duże zniszczenia podczas II wojny światowej ( bombardowania 4 marca 1945 ) .
Pod gruzami znalazła się większa część tkanki urbanistycznej ( w tym barokowe zabytki ) .
W mieście prawdopodobnie urodził się Marcin z Tours .
Kultura .
W mieście działa ( `` Weöres Sándor Színház '' ) .
Sport .
W mieście ma siedzibę klub piłkarski Szombathelyi Haladás , brązowy medalista ligi węgierskiej sezonu 2008/2009 .
