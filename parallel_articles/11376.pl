1 stycznia 1946 gmina składała się z samej siedziby i przez to nie była podzielona na gromady .
Jako gmina wiejska jednostka przestała istnieć 1 stycznia 1947 wraz z nadaniem Szopienicom praw miejskich i przekształceniem jednostki w gminę miejską .
W związku z likwidacją powiatu katowickiego 1 kwietnia 1951 Szopienice zostały powiatem miejskim do których włączono zniesione gminy Dąbrówka Mała i Janów , a następnie 31 grudnia 1959 weszły w skład Katowic .
