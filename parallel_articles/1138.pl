Amanita ovoidea
Amanita ovoidea ( Bull . )
Link – gatunek grzybów z rodziny muchomorowatych ( `` Amanitaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Amanita , Amanitaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1788 r. Jean Baptiste Bulliard nadając mu nazwę `` Agaricus ovoideus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1833 r. Johann Heinrich Friedrich Link , przenosząc go do rodzaju `` Amanita '' .
Niektóre synonimy ) :
Charakterystyka .
Występuje w Europie , w lasach mieszanych na glebach wapiennych , bardzo rzadki .
W wielu rejonach świata gatunek uznawany jest za jadalny i smaczny , znane są jednak przypadki zatrucia , dlatego odradza się jego spożywanie .
