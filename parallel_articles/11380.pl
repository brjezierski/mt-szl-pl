Sołectwo
Sołectwo – jednostka pomocnicza gminy we współczesnej Polsce , charakterystyczna głównie dla obszarów wiejskich .
Obszar , zakres działania sołectwa i jego organów określa rada gminy w statucie sołectwa .
Według stanu z dnia 31 grudnia 2021 roku w Polsce było 40 825 sołectw .
Charakterystyka .
Organem uchwałodawczym w sołectwie jest zebranie wiejskie , a wykonawczym – sołtys .
Działalność sołtysa wspomaga rada sołecka .
Sołtysa oraz radę sołecką wyłaniają stali mieszkańcy danego sołectwa w tajnym i bezpośrednim głosowaniu na zebraniu wiejskim .
Zebranie wiejskie jest przykładem realizacji idei demokracji bezpośredniej , podczas którego artykułowane są najważniejsze problemy mieszkańców sołectwa .
