Historia .
Począwszy od IX wieku p.n.e .
Półwysep Iberyjski był terenem ekspansji licznych ludów i państw : Celtów , Fenicjan , Greków czy też Kartagińczyków .
W II wieku p.n.e .
na półwyspie pojawili się Rzymianie .
W V wieku na tereny obecnej Hiszpanii wdarli się Wizygoci i stworzyli tam własne królestwo .
W roku 711 z obszarów północnej Afryki na Półwysep Iberyjski przedostały się wojska arabskie pod wodzą Tarika , które błyskawicznie podbiły prawie cały półwysep .
Niewielkie królestwa chrześcijańskie na północy rozpoczęły rekonkwistę , która zaowocowała ostatecznym wyparciem Maurów w 1492 .
Przywódcą i pierwszym królem , zgodnie z tradycją , był don Pelayo , prawdopodobnie szlachcic wizygocki , który razem z innymi schronił się w górach Asturii przed napierającymi z południa wojskami muzułmańskimi .
Jako początek procesu rekonkwisty uznaje się bitwę w wąwozie Covadonga ( 722 ) , w której oddział ( około 300 ludzi ) pod wodzą don Pelayo pokonał dzięki zaskoczeniu i pułapce kilkukrotnie większe siły przeciwnika .
Rozbity oddział muzułmański miał charakter rozpoznawczy , a jego „ klęska ” nie miała dla najeźdźców większego znaczenia .
W ten sposób powstało pierwsze minikrólestwo chrześcijańskie , które rozpoczęło powolne wypieranie najeźdźców muzułmańskich .
Z tych drobnych państw chrześcijańskich z czasem wyłoniły się dwa silne królestwa : Kastylia i Aragonia .
Rozkwit i ekspansja powstałej z połączenia obu tych państw Hiszpanii były przede wszystkim związane z podbojami kolonialnymi w Ameryce i zdobyciem wielkich bogactw .
