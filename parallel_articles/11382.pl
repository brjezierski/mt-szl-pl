Hiszpania
Hiszpania , Królestwo Hiszpanii ( hiszp .
`` España '' , `` Reino de España '' ) – państwo w zachodniej części Europy Południowej , położone głównie na Półwyspie Iberyjskim , obejmujące także Baleary na Morzu Śródziemnym , Wyspy Kanaryjskie na Oceanie Atlantyckim oraz mniejsze wysepki i tereny w północnej Afryce .
Składa się z 17 regionów autonomicznych i dwóch miast autonomicznych .
Stolicą i największym miastem jest Madryt , duże znaczenie ma również Barcelona i Walencja .
Państwo ma powierzchnię 505 944 km² , będąc czwartym co do wielkości państwem w Europie , po Rosji , Ukrainie i Francji .
Ludność oscyluje na poziomie 50 mln .
Dominuje w nim klimat subtropikalny .
Na zachodzie Hiszpania graniczy z Portugalią , na południu z należącym do Wielkiej Brytanii Gibraltarem , oraz przez plazas de soberanía z Marokiem .
Na północnym wschodzie , przez Pireneje , kraj graniczy z Francją i Andorą .
Północno-zachodnia i południowo-zachodnia część leży nad Oceanem Atlantyckim , pozostała część wybrzeża leży nad Morzem Śródziemnym .
Hiszpania ma długą historię i bogate dziedzictwo kulturowe .
Przez setki lat była częścią Starożytnego Rzymu , w XV wieku stworzyła własne Imperium Hiszpańskie , jedne z największych imperiów w historii świata – imperium , nad którym nigdy nie zachodzi słońce .
Dzisiejsza Hiszpania jest krajem rozwiniętym o bardzo wysokim wskaźniku rozwoju społecznego i dobrej jakości życia ( 10 miejsce na świecie ) .
Jest członkiem Unii Europejskiej ( od 1986 roku ) oraz kilkudziesięciu organizacji międzynarodowych , w tym ONZ , NATO , OBWE , WTO , OEI i Unii Łacińskiej .
Obowiązującą walutą jest euro .
Znajduje się tu 47 obiektów wpisanych na listę światowego dziedzictwa UNESCO ( 3 miejsce na świecie ) .
Jest drugim najczęściej odwiedzanym państwem na świecie , w 2017 roku Hiszpanię odwiedziło 82 mln turystów .
Etymologia .
Nazwa Hiszpania ( hiszp .
`` España '' ) wywodzi się od nazwy `` Hispania '' , którą Rzymianie nadali części Półwyspu Iberyjskiego .
Alternatywną nazwą była `` Iberia '' – nazwa preferowana przez greckich pisarzy w odniesieniu do tej samej przestrzeni , wywodziła się ona od rzeki `` Iber '' ( hiszp .
`` Ebro '' ) lub od słowa , które Grecy słyszeli od miejscowych ludów , również na południu półwyspu .
Jednakże fakt , że termin `` Hispania '' jest terminem łacińskim doprowadził do sformułowania kilku teorii na temat jego pochodzenia .
« Hispania » wywodzi się od fenickiej nazwy `` i-spn-ya '' , terminu którego stosowanie jest odnotowane w dokumentach dopiero począwszy od drugiego tysiąclecia p.n.e .
Fenicjanie byli przedstawicielami pierwszej wyższej cywilizacji zamieszkującej Półwysep Iberyjski , przybyli tu z terenów dzisiejszej Syrii .
To oni założyli na terenie dzisiejszej Hiszpanii miasto `` Gadir '' dzisiejszy Kadyks , najstarsze miasto Europy Zachodniej .
Po pokonaniu Kartagińczyków przez Rzymian , osadnicy nazwali te tereny `` wyspą góralków/góralkową '' , gdyż po przypłynięciu zobaczyli tu mnóstwo królików i pomylili je z góralkami , które znali z Afryki , wówczas to wzięli je za znane z Palestyny góralki , które w języku fenickim , należącym do grupy języków semickich , musiały nazywać się , podobnie jak w biblijnym języku hebrajskim właśnie אי שפנים ( `` '' / '' Hi-szfanim '' ) , w liczbie mnogiej `` szaphanim '' .
Określili oni wówczas tereny te jako `` tierra abundante en conejos '' – ziemia bogata w króliki .
Na monecie z Galby widnieje uosobienie Hiszpanii z królikiem u stóp .
Fenickie `` Iszaphanim '' zlatynizowało się do `` Hispania '' już w czasach starożytnych .
Stąd mamy współczesne określenia Hiszpanii , które wyewoluowały od tej właśnie nazwy .
Inna teoria pochodzenia nazwy wskazuje na preromańską nazwę Sewilli – `` Hispalis '' .
Inne hipotezy zakładają , że zarówno nazwy `` Hispalis '' , jak `` Hispania '' pochodzą od nazw dwóch legendarnych królów Hiszpanii .
Hispalo i jego syn Hispano lub Hispan , odpowiednio syn i wnuk Heraklesa .
