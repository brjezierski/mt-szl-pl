Sklejka
Sklejka ( potocznie dykta , z niem .
`` Dickte '' ) – materiał kompozytowy sklejony z krzyżujących się cienkich warstw drewna ( obłogów ) .
Zwykle składa się z nieparzystej ich liczby .
Sklejki wytwarza się z różnych gatunków drewna , najczęściej z brzozy , olchy , sosny , rzadziej z buku lub z drzew egzotycznych .
Wewnętrzne warstwy sklejki często są z innego , tańszego gatunku niż zewnętrzne .
Ze względu na rodzaj zastosowanego w produkcji kleju wyróżnia się sklejki suchotrwałe i wodoodporne ( wodoodporność odnosi się do kleju , a nie całego tworzywa , które jako produkt drewniany nie jest odporne na wodę ) .
Sklejka daje się względnie łatwo modelować ( wyginać ) na gorąco , co pozwala na wytwarzanie kształtów przydatnych np .
w meblarstwie .
Dzięki temu sklejka jest używana powszechnie do produkcji mebli domowych i biurowych , a także w budownictwie , do produkcji opakowań i w szkutnictwie .
Sklejka lotnicza różni się od zwykłej lepszą jakością , dokładnością wykonania i wytrzymałością .
Zawsze jest klejona klejem wodoodpornym i prawie zawsze wytwarzana jest z brzozy .
O precyzji wykonania sklejki lotniczej świadczy fakt , że najcieńsza sklejka tego typu ma grubość 0,4 mm i sklejona jest z trzech warstw drewna .
Obecnie w lotnictwie w bardzo ograniczonym zakresie wykorzystuje się sklejkę – praktycznie tylko na elementy drugorzędne lub do remontu starych szybowców .
Niewielkie jej ilości używane są przez modelarzy .
W czasie II wojny światowej produkowany był z drewna – głównie ze sklejki , brytyjski samolot myśliwsko-bombowy De Havilland Mosquito .
