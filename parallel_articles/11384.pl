Gra fabularna
Gra fabularna ( inaczej RPG , z ang .
`` role-playing game '' , nieraz zwana `` grą wyobraźni '' , potocznie `` erpegiem '' lub `` rolplejem '' ) – gra towarzyska oparta na narracji , w której gracze ( od jednego do kilku ) wcielają się w role fikcyjnych postaci .
Cała rozgrywka toczy się zazwyczaj w fikcyjnym świecie , istniejącym tylko w wyobraźni grających .
Jej celem na ogół jest rozegranie gry według zaplanowanego scenariusza i osiągnięcie umownie określonych lub indywidualnych celów , przy zachowaniu wybranego zestawu reguł , zwanego mechaniką gry .
