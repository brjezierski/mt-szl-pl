„ pen and paper ” , istnieją też ich elektroniczne odpowiedniki – komputerowe gry fabularne .
Wstęp .
Klasyczną grę fabularną można porównać do teatru improwizacji , w którym osoba prowadząca ( zwana mistrzem gry ) odgrywa rolę autora scenariusza , reżysera , narratora i zazwyczaj aktora ról postaci drugoplanowych .
Natomiast gracze , czyli osoby biorące udział w grze , są aktorami pierwszoplanowymi , nieznającymi wszakże scenariusza i zmuszonymi improwizować oraz reagować na sytuacje , jakie stawia przed nimi mistrz gry .
Jeżeli graczy jest wielu , mogą dobierać się w drużyny .
Natomiast jeżeli w rozgrywce bierze udział wyłącznie jeden gracz , mistrz gry organizuje tzw .
solówkę .
Cała rozgrywka toczy się w ramach spotkań – sesji – odbywających się w odpowiednich pomieszczeniach .
Rozgrywka jest konstruowana prawie wyłącznie w oparciu o słowne opisy , czasem wspomagane muzyką lub taktyczną planszą .
