W grach polegających na budowie miast gracz z reguły obserwuje swoją metropolię w rzucie izometrycznym lub z góry .
Gracz wybiera rodzaje budynków i ich rozmieszczenie oraz zarządza rozmaitymi aspektami społeczno-gospodarczymi , takimi jak wysokość podatków , infrastruktura czy miejsca pracy .
