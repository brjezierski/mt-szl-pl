Gra w budowę miasta
Gra w budowę miasta – gatunek gier komputerowych , w którym gracz pełni funkcję planisty i przywódcy miasta odpowiedzialnego za jego zarządzanie i rozwój .
Produkcje tego typu łączą w sobie elementy gier ekonomicznych , strategicznych i symulacji .
