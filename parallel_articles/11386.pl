Mecz
Mecz ( ang .
`` match '' ) – podstawowa wielkość rozgrywki w wielu sportach .
Mecze limitowane są czasem lub różnicą zdobytych punktów zgodnie z regulaminem danej dyscypliny sportu i oceniane są przez sędziego lub ich kolektyw .
Mecz składa się zazwyczaj z mniejszych części .
Sam natomiast jest częścią większych rozgrywek np .
mistrzostw kraju , mistrzostw kontynentu , mistrzostw świata , pucharów , igrzysk olimpijskich , spartakiad .
Ze względu na liczbę zawodników rozróżniamy mecze indywidualne i drużynowe , zwane czasami zespołowymi .
