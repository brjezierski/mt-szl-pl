Część pierwsza była trzecim obrazem autora , przy czym dwa poprzednie zaginęły .
Fabuła .
Część I .
Kay Hoog znajduje wiadomość w butelce z mapą prowadzącą do inkaskiego skarbu , co owocuje wyjazdem do Peru w poszukiwaniu złota .
Przedstawiciele tajnej organizacji `` Pająki '' pod wodzą Lio Sha kradną jednak mapę , ale Hoog odzyskuje ją już na terenie Peru , przy okazji ratując kapłankę Sun Nael , którą `` Pająki '' uwięziły .
Hoog zakochuje się w Sun i jedzie z nią do San Francisco , ale śledzą ich `` Pajaki '' , zabijając kapłankę , za co Hoog przysięga im zemstę .
Część II .
Hoog planuje zemstę i uzyskuje dostęp do tajnego miejsca spotkań `` Pająków '' , którzy poszukują w tej części wielkiego diamentu , głowy Buddy , która według legendy daje swojemu właścicielowi panowanie nad całą Azją .
Szlak poszukiwań prowadzi na Falklandy i do handlarza diamentami , Johna Terry'ego w Londynie .
Dochodzi do dramatycznego spotkania między Hoog i Lio Sha w jaskini ze skarbami .
Charakterystyka .
Planowany serial nie został nigdy ukończony – zrealizowano tylko dwa pierwsze odcinki .
Mimo skąpego materiału , według historyka kina , Lotte H. Eisner , zadziwia wielowątkowością , rozkwitem motywów pobocznych , mnogością zazębiających się z sobą przygód , które częściowo utrudniają zrozumienie akcji i orientowanie się w jej bieżącym przebiegu .
Również treść napisów jest nie do końca w tym kontekście dla widza zrozumiała .
Mimo mankamentów obraz jest dowodem mistrzostwa reżyserskiego młodego wówczas Langa i posiada już wszystkie cechy jego warsztatu .
Sceny kręcone w jadącym pociągu , czy dynamiczne pogonie utrzymują widza w napięciu , są precyzyjne , logicznie skomponowane , epatują nieprzewidywalnością , a także znajomością zagadnień scenografii i oświetlenia .
Do słabszych elementów serialu należy niezbyt jeszcze wyszukany komizm autora , sztuczna i upozorowana gra Ressel Orli , którą jednak równoważą w części znakomicie prezentujący się aktorzy drugiego planu .
