Są też elementy , które zbliżają w niektórych ujęciach obraz do westernu .
Moment napadu na bank , czy wtargnięcie złodziei do domu zwiastują natomiast formalnie zdjęcia do `` M – Morderca '' z 1931 .
Podziemne miasto Inków przywodzi na myśl szkic do katedry z `` Metropolis '' .
