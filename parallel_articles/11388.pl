Szpakowate
Szpakowate ( Sturnidae ) – rodzina ptaka z rzędu wróblowych ( Passeriformes ) , obejmująca około 120 gatunków .
Występowanie .
Rodzina obejmuje gatunki występujące w Eurazji , Afryce , Oceanii i północno-wschodniej Australii .
Introdukowane w wielu rejonach świata .
Systematyka .
Taksonem siostrzanym dla szpakowatych są przedrzeźniacze ( Mimidae ) .
