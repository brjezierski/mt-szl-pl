Muchomor wiosenny
Muchomor wiosenny ( `` Amanita verna '' ( Bull . )
Lam . )
– gatunek grzybów należący do rodziny muchomorowatych ( `` Amanitaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Amanita , Amanitaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1780 r. Bulliard nadając mu nazwę `` Agaricus bulbosus '' f. `` vernus '' .
W 1783 r. podniósł go do rangi gatunku jako `` Agaricus vernus '' .
W tym samym roku Lamarck przeniósł go do rodzaju `` Amanita '' , nadając mu obecną , uznaną przez Index Fungorum nazwę .
Synonimy naukowe :
W niektórych publikacjach klasyfikowany jest jako jego odmiana wiosenna : `` Amanita phalloides '' var .
`` verna '' , jednak według Index Fungorum obecnie w obrębie taksonu `` Amanita phalloides '' nie wyróżnia się żadnego podgatunku ani odmiany .
