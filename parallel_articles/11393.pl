Geneza .
Szron powstaje w analogicznych warunkach jak rosa jednak w ujemnych temperaturach w wyniku resublimacji pary wodnej zawartej w atmosferze na powierzchniach poziomych lub lekko skośnych jak grunt , trawa , szyba auta .
Podobnie jak w przypadku rosy wyróżnia się szron właściwy i adwekcyjny .
Szron nie pojawia się jedynie w okresie letnim w tzw .
okresie bezprzymrozkowym .
Jest obserwowany najczęściej jesienią i wiosną , w zimie może pojawić się na powierzchni pokrywy śnieżnej .
Wystąpienie szronu w przejściowych porach roku może być wskazówką na temat warunków mikroklimatycznych panujących w danej okolicy .
Częste występowanie szronu w danym miejscu może wskazywać na mrozowisko .
