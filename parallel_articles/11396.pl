Dolina Trzech Stawów
Dolina Trzech Stawów ( niem .
: `` Stauweiher '' , śl : `` Sztauwajery '' ) − park i kompleks rekreacyjno-sportowy , znajdujący się w Katowicach , w jednostce pomocniczej osiedle Ignacego Paderewskiego−Muchowiec .
Dolina Trzech Stawów ma powierzchnię 86 ha , w tym 9 ha powierzchni wodnej .
Jest to obszar położony w sąsiedztwie obszarów zurbanizowanych , dość intensywnie wykorzystywany rekreacyjnie .
Istnieje tu duże zróżnicowanie siedliskowe : trzy gatunki roślin chronionych i kilka gatunków chronionych płazów .
Jest to miejsce żerowania na trasie przelotu wielu gatunków ptaków .
Nazwa kompleksu wzięła się od trzech wyróżniających się wielkością zbiorników wodnych ( stawy : Kąpielowy , Kajakowy i Łąka ) , jakie powstały w dolince wytworzonej przez Potok Leśny , prawy dopływ Rawy .
Z doliną sąsiaduje lotnisko Katowice-Muchowiec , którego głównym użytkownikiem jest Aeroklub Śląski .
Na terenie parku znajduje się parking , liczne ścieżki rowerowe , przystań wodna z boiskami do siatkówki plażowej ( ul .
Murckowska 215 ) oraz stacja roweru miejskiego .
