Tramwaje w Szczecinie
Tramwaje w Szczecinie – system transportu tramwajowego działający w Szczecinie .
Składa się on z dwunastu linii dziennych oraz sezonowej linii turystycznej „ 0 ” , obsługiwanej taborem historycznym .
Łączna długość linii wynosi 114 km , a rozstaw szyn 1435 mm .
W ich przebiegu znajdują się dwie zajezdnie – Golęcin i Pogodno oraz czternaście pętli ( w tym sześć ulicznych ) .
Linie zwykłe , dzienne ( nocne zostały zlikwidowane 1 grudnia 1996 r. ) obsługuje spółka Tramwaje Szczecińskie , na zlecenie Zarządu Dróg i Transportu Miejskiego , a linię turystyczną STMKM z inicjatywy własnej .
W 2011 r. udział tramwajów w przewozach pasażerów miejskiego transportu publicznego wynosił 44 % .
Tramwaje Szczecińskie .
Miejski Zakład Komunikacyjny w Szczecinie był zakładem budżetowym , który powstał 1 czerwca 1995 roku po wydzieleniu z Miejskiego Zakładu Usług Komunalnych .
Z dniem 1 stycznia 2009 r. zastąpiła go spółka Tramwaje Szczecińskie .
Organem reprezentującym TS jest zarząd , którego dyrektorem od 2009 r. jest Krystian Wawrzyniak .
