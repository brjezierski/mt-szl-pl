Amasya
Amasya – miasto w Turcji , stolica prowincji Amasya .
Według danych na rok 2000 miasto zamieszkiwało 74 393 osób , a według danych na rok 2004 całą prowincję ok. 361 000 osób .
Gęstość zaludnienia w prowincji wynosiła ok. 65 osób na km² .
W mieście rozwinął się przemysł włókienniczy , spożywczy oraz ceramiczny .
