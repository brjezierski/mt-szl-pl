Sztokholm ( region )
Sztokholm – jeden ze szwedzkich regionów terytorialnych ( `` szw . ''
län ) .
Graniczy z regionami Uppsala i Södermanland oraz z Morzem Bałtyckim .
Siedziba władz regionu – Sztokholm – jest jednocześnie stolicą Szwecji .
Region Sztokholm położony jest we wschodniej części Svealand i obejmuje północno-wschodnią część prowincji historycznej ( `` landskap '' ) Södermanland i południowo-wschodnią część Uppland .
