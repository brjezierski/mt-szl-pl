Tramwaj ( film )
Tramwaj – czarno-biała , niema etiuda produkcji polskiej z 1966 roku , w reżyserii Krzysztofa Kieślowskiego .
Reżyserski debiut Kieślowskiego .
Opis fabuły .
Młody mężczyzna źle się czuje na prywatce i opuszcza zabawę .
Po wyjściu z mieszkania goni nocny tramwaj , a następnie jedzie nim wraz z samotną dziewczyną .
Młodzi pomimo nawiązania wzrokowego kontaktu , nie zapoznają się ze sobą .
Chłopak wysiada na swoim przystanku , a tramwaj z dziewczyną odjeżdża .
Chłopak ponownie goni pojazd , ale tym razem bez powodzenia .
