Prąd elektryczny
Prąd elektryczny – uporządkowany ruch ładunków elektrycznych .
W naturze przykładami są wyładowania atmosferyczne , wiatr słoneczny czy czynność komórek nerwowych , którym również towarzyszy przepływ prądu .
W technice obwody prądu elektrycznego są masowo wykorzystywane w elektrotechnice i elektronice .
Prąd elektryczny w materii .
Pod wpływem pola elektrycznego ( przyłożonego napięcia ) w materiałach , w których istnieją ruchliwe nośniki ładunku dochodzi do zjawiska przewodzenia prądu elektrycznego .
Przewodniki i izolatory .
Materiały , które dobrze przewodzą prąd elektryczny to przewodniki .
Oporność właściwa dobrych przewodników jest rzędu od 10−8 do 10−6 Ω·m .
Dielektryk ( izolator elektryczny ) to materiał , w którym bardzo słabo przewodzony jest prąd elektryczny .
Może to być rezultatem niskiej koncentracji ładunków swobodnych , niskiej ich ruchliwości lub obu tych czynników równocześnie .
Oporność właściwa dielektryków jest większa od 106 Ω·m .
Półprzewodniki mają oporność właściwą pośrednią między metalami a izolatorami .
Ich przewodnictwo zwykle mocno rośnie ze wzrostem temperatury .
Specyficzną formą przewodnictwa jest nadprzewodnictwo – występujący w niektórych materiałach efekt sprawiający , że w odpowiednio niskiej temperaturze ma on zerową rezystancję .
W nadprzewodnikach zachodzą również inne zjawiska , na przykład efekt Meissnera .
Większość materiałów wykazuje nadprzewodnictwo dopiero w bardzo niskiej temperaturze .
Nośniki ładunku .
Prąd elektryczny jest w istocie ruchem cząstek obdarzonych ładunkiem , zwanych nośnikami ładunku .
Umownie przyjęło się określać kierunek przepływu prądu poprzez opisanie ruchu ładunków dodatnich , niezależnie od tego jaki jest rzeczywisty znak i kierunek ruchu nośników w danym materiale .
W metalach ( zarówno stałych , jak i w stanie ciekłym ) nośnikami ładunku są elektrony .
Elektrony , znajdujące się w paśmie przewodnictwa , mogą swobodnie się przemieszczać w objętości metalu .
Dlatego wszystkie metale są dobrymi przewodnikami , a prąd elektryczny w metalach jest ruchem elektronów przewodnictwa .
W konwencjonalnych półprzewodnikach ( takich jak krzem czy german ) w temperaturze zera bezwzględnego nie ma elektronów w paśmie przewodnictwa .
