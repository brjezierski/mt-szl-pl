Aksamitka rozpierzchła
Aksamitka rozpierzchła ( `` Tagetes patula '' L. ) – gatunek rośliny z rodziny astrowatych .
Inne jej nazwy zwyczajowe : aksamitek rozpierzchły , aksamitek drobnokwiatowy , szarańcza aksamitek , afrykanki , tagetes rozpierzchły .
Swoje pochodzenie wywodzi z Meksyku i Nikaragui , skąd gatunek ten został rozpowszechniony jako ozdobna roślina ogrodowa .
Odmiany ozdobne .
Ogrodnicy wyhodowali wiele kultywarów o bardziej ozdobnych kwiatostanach , niż typowa forma .
Należą do nich m.in .
:
Zastosowanie .
Jako roślina ozdobna ma wszechstronne zastosowanie .
Nadaje się na rabaty kwiatowe , na obwódki rabat .
Może być uprawiana w pojemnikach na balkonach , tarasach itp .
Może być uprawiana jako roślina zadarniająca .
Uprawiana bywa na grobach ziemnych na cmentarzach .
Dobrze też komponuje się z innymi roślinami , może być np .
sadzona w pojemnikach w kompozycji z innymi roślinami ozdobnymi .
Wysuszone i pocięte lub sproszkowane płatki aksamitki rozpierzchłej pod nazwą szafran imeretyński stosuje się jako przyprawę w kuchniach Kaukazu , przede wszystkim Gruzji .
Uprawa .
Roślina łatwa w uprawie .
Wymaga stanowisk w pełnym słońcu i żyznej oraz przepuszczalnej gleby .
Rozmnaża się z nasion wysiewanych do rozsadników wczesną wiosną .
Chcąc , by zakwitła wcześniej lepiej jest kupić gotowe sadzonki wyhodowane przez specjalistów pod osłonami .
Rośliny mogą być atakowane przez ślimaki , a przy długotrwałych deszczach przez szarą pleśń ( zapobiega się jej chemicznie , przez opryskiwanie preparatami grzybobójczymi ) .
Nawozić należy nawozami z niewielką ilością azotu , gdyż obfite nawożenie azotowe powoduje bujny wzrost rośliny , ale słabe jej kwitnienie .
