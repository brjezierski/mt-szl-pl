Sztutowo
Sztutowo ( kaszb .
`` Sztutowò '' , niem .
`` Stutthof '' ) – wieś położona w województwie pomorskim , w powiecie nowodworskim , w gminie Sztutowo na obszarze Żuław Wiślanych przy drodze wojewódzkiej nr 501 .
Wieś jest siedzibą sołectwa Sztutowo , w którego skład wchodzi również miejscowość Grochowo Drugie .
Miejscowość jest siedzibą gminy Sztutowo .
Pomiędzy Sztutowem a Kątami Rybackimi znajduje się rezerwat kormoranów czarnych .
Wieś jest ośrodkiem wczasowym i krajoznawczym .
Wieś należąca do Mierzei Wiślanej terytorium miasta Gdańska położona była w drugiej połowie XVI wieku w województwie pomorskim .
W latach 1975–1998 miejscowość położona była w województwie elbląskim .
Na terenie miejscowości znajduje się rzymskokatolicka parafia św .
Wojciecha .
Jej świątynią jest dawny kościół baptystyczny wzniesiony w latach 1860–1878 .
