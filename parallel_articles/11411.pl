Kwadrans
Kwadrans ( łac .
`` quadrans '' , czwarta część ) – ćwierć godziny , czyli piętnaście minut .
Kwadrans akademicki to zwyczaj na wyższych uczelniach , który pozwala na opuszczenie bez żadnych konsekwencji studentom zajęć , gdy prowadzący spóźnia się ponad piętnaście minut .
Ten sam zwyczaj pozwala studentom na spóźnienie się na zajęcia do 15 minut , bez ponoszenia negatywnych konsekwencji .
Do 15 minut nawiązuje nazwa litewskiego portalu informacyjnego , 15min .
