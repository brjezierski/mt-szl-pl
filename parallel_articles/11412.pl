Czwartek
Czwartek ( skrót czw . )
– dzień tygodnia między środą a piątkiem .
Według normy ISO-8601 jest czwartym dniem tygodnia .
Niegdyś był to dzień wolny od zajęć dla żaków .
W kulturze ludowej ziem Polski czwartek obwarowany jest zakazami dotyczącymi sprzątania oraz przędzenia , których egzekutorką miała być bogini Marzanna .
W tradycji biblijnej – chrześcijańskiej i judaistycznej , gdzie za pierwszy dzień tygodnia uznawana jest niedziela , czwartek jest dniem piątym .
