Sztum
Sztum ( ) – miasto w północnej Polsce , w województwie pomorskim .
Siedziba powiatu sztumskiego oraz gminy miejsko-wiejskiej Sztum .
Położone nad jeziorami : Zajezierskim i Barlewickim ( dawniej tworzyły jedno Jezioro Białe ) , przy drodze krajowej nr 55 , pomiędzy Malborkiem a Kwidzynem .
Według danych GUS z 30 czerwca 2021 r. , Sztum liczył 9746 mieszkańców i był pod względem liczby ludności 25. miastem w województwie pomorskim .
Położenie .
Sztum leży w historycznych Prusach , w Pomezanii , na obszarze dawnej ziemi malborskiej , a także etnograficznie na Powiślu .
Według regionalizacji geograficznej Sztum należy do zachodniej części Pojezierza Iławskiego .
Powierzchnia miasta wynosi 4,59 km² .
Komunikacja i transport .
W Sztumie znajduje się stacja kolejowa , obsługująca połączenia m.in .
z Trójmiastem , Grudziądzem , Malborkiem i Kwidzynem .
