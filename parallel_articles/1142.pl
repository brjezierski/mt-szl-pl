Amazonka ( obraz Piotra Michałowskiego )
Amazonka – obraz olejny polskiego malarza Piotra Michałowskiego z 1853-1855 roku , znajdujący się w Galerii sztuki polskiej 1800-1945 Muzeum Śląskiego w Katowicach .
Tytuł , określenie kobiety jeżdżącej na koniu , nawiązuje do mitologicznych Amazonek .
Autor przedstawił na obrazie Celinę Michałowską ( 1837-1916 ) , swoją córkę .
Nastolatka w czarnej sukni jedzie na białym koniu , mając w tle nieokreślony fragment krajobrazu i ciemne niebo .
Muzeum zakupiło obraz od wnuka malarza Dominika Łempickiego , syna jego córki Marii oraz Adama Łempickiego .
W skład kolekcji obraz wszedł w 1930 roku .
Działo o wymiarach 63 x 49,5 cm nie jest sygnowane .
Muzealny numer inwentarzowy : MŚK/SzM/435 .
Około 1853 roku Piotr Michałowski namalował drugą córkę `` Portret córki artysty Celiny na koniu '' .
To dzieło stanowi część kolekcji Muzeum Narodowego w Warszawie .
Poczta Polska wydała w 28 września 1972 znaczek o nominale 0,30 zł z reprodukcją `` Amazonki '' w serii `` Malarstwo polskie – Dzień Znaczka 1972 '' .
Nakład liczył 8 515 045 sztuk .
Druk wykonano techniką rotograwiury na papierze kredowanym .
Autorem projektu znaczka był Tadeusz Michaluk .
Znaczek wyszedł z obiegu w 1994 roku .
