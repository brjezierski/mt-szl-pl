Szuć
Szuć ( niem .
`` Schuttschen '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie szczycieńskim , w gminie Jedwabno .
Wieś jest placówką Ochotniczej Straży Pożarnej .
Na południe od miejscowości przepływa obszarami leśno-bagiennymi rzeka Omulew .
Znajdują się tu pozostałości po nieistniejących już wioskach Dębowiec Mały , Dębowiec Duży i Ruda , a także pobliski rezerwat przyrody Małga .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
