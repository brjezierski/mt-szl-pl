Szwajcaria ( Suwałki )
Szwajcaria – część miasta Suwałki , jednostka pomocnicza gminy miejskiej ( sołectwo ) .
Stanowi przyłączoną w całości do Suwałk w 1981 r. dotychczasową wieś Szwajcaria .
Pochodzi stamtąd polityk Jarosław Zieliński .
Znajduje się tu skansen archeologiczny obejmujący kurhany i cmentarzysko na terenie rezerwatu przyrody Cmentarzysko Jaćwingów .
W pobliżu źródła ma Kanał Kamionka .
