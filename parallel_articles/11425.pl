Szwecja
Szwecja , Królestwo Szwecji ( , `` Konungariket Sverige '' ) – państwo w Europie Północnej , zaliczane zarówno do państw skandynawskich oraz krajów nordyckich .
Szwecja jest członkiem Unii Europejskiej od 1995 roku .
Z około 10,3 mln mieszkańców jest najludniejszym krajem skandynawskim .
Graniczy z Norwegią , Finlandią oraz z Danią przez most nad cieśniną Öresund .
Historia .
Średniowiecze .
Południowe obszary współczesnej Szwecji ( Skania ) były częścią praojczyzny Germanów .
W średniowieczu północna i środkowa część kraju została zasiedlona przez skandynawskie plemiona Normanów .
W IX–XI wieku tutejsi wikingowie brali udział w wyprawach łupieżczych , głównie na Ruś i Bizancjum .
Równocześnie tworzyło się państwo szwedzkie i następowała jego chrystianizacja w XI wieku .
W XII–XIII w. podbój Finlandii przez Szwedów .
Od 1397 unia kalmarska z Danią i Norwegią .
XVI wiek .
W 1523 Gustaw I Waza doprowadził do usamodzielnienia się Szwecji pod władzą dynastii Wazów .
W 1527 przyjęto luteranizm .
W latach 1592–1599 Szwecja była połączona unią polsko-szwedzką z Rzecząpospolitą Obojga Narodów .
XVII wiek .
W tym czasie miał miejsce podbój części Łotwy i Estonii .
W 1635 ( od Rozejmu w Sztumskiej Wsi ) Szwecja włączyła do swojego terytorium całą Estonię .
Od panowania Gustawa II Adolfa do Karola X Gustawa ( 1654–1660 ) miała miejsce walka o dominację w rejonie Morza Bałtyckiego .
Wojny z Danią , Polską i Rosją oraz w latach 1630–1648 udział w wojnie trzydziestoletniej , która doprowadziła do uzyskania przez Szwecję pozycji mocarstwowej .
XVIII wiek .
Po klęsce Karola XII w wojnie północnej ( 1700–1721 ) doszło do utraty większości zdobyczy ( głównie na rzecz Rosji ) oraz utraty rangi mocarstwa ( Pokój w Nystad 1721 ) .
Po śmierci Karola w roku 1718 nastała tak zwana era wolności ( 1718–1772 ) , czyli okres rozkwitu parlamentaryzmu , przy jednoczesnym zmniejszeniu politycznych wpływów tronu .
W pierwszej połowie tej epoki dwie frakcje ; „ partia kapeluszy ” i „ partia czapek ” rywalizowały o władzę i wpływy .
Ważnym wydarzeniem epoki było zniesienie cenzury w Szwecji ( 1766 ) .
Era wolności dobiegła końca , gdy Gustaw III odnowił władzę absolutną drogą zamachu stanu .
XIX wiek .
W 1805 roku Szwecja dołączyła do III koalicji antyfrancuskiej .
Działania wojenne szwedzka armia prowadziła na terytorium Pomorza , ale nie odniosła żadnych sukcesów .
W 1808 roku , wskutek lekkomyślnej polityki króla Gustawa IV Adolfa Szwecja zerwała rozejm z Francją oraz wplątała się w wojnę z Danią i Rosją .
Konsekwencją tego była utrata Finlandii na rzecz Rosji .
W 1809 roku , po obaleniu króla Gustawa Adolfa , parlament uchwalił pierwszą w dziejach Szwecji konstytucję .
W 1812 roku nowy król Karol XIV Jan doprowadził do zbliżenia z Rosją .
Wojska szwedzkie wzięły udział w walkach przeciwko Napoleonowi .
Największym jednak sukcesem nowego króla było , po zwycięskiej wojnie z Danią , przyłączenie Norwegii do Szwecji .
Przyłączenie to nie miało charakteru inkorporacji , ale unii personalnej dwóch równorzędnych krajów .
W 1866 roku wprowadzono reformę parlamentu , który znosił podział na stany .
Reforma ta uczyniła Szwecję krajem liberalnym .
Druga połowa XIX wieku to czasy wzrostu znaczenia przemysłu i masowej urbanizacji , która przekształciła Szwecję z kraju rolniczego w kraj uprzemysłowiony .
XX wiek .
Zmiany ustrojowe rozpoczęte już w drugiej połowie XIX wieku i na początku XX wieku , takie jak reforma parlamentu 1866 , powstanie nowoczesnych partii , wprowadzenie rządów parlamentarnych , powszechne prawo wyborcze 1919 uczyniły ze Szwecji liberalne i nowoczesne państwo .
W 1905 roku od Szwecji odłączyła się Norwegia , kładąc kres długoletniej unii .
Szwecja zachowała neutralność podczas I i II wojny światowej , co uchroniło ją od zniszczeń i kosztów wojennych oraz wzmocniło gospodarkę .
Od lat 30. władzę sprawowali głównie socjaldemokraci z partii SAP .
Ich rządy przyniosły liczne reformy socjalne , przeprowadzone w duchu progresywizmu .
