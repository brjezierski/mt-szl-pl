Szwedzi
Szwedzi ( szw .
`` svenskar '' ) – naród germański mieszkający głównie w Szwecji ( około 9 mln ) , zachodniej i południowo-zachodniej Finlandii ( około 300 tys . )
oraz w innych krajach skandynawskich , w USA ( ponad 4 miliony ludzi pochodzenia szwedzkiego ) i Kanadzie .
Około 90 % schrystianizowanych w XI-XII wieku Szwedów należy od XVI wieku do Szwedzkiego Kościoła Ewangelicko-Luterańskiego ( w 2000 doszło do separacji kościoła od państwa ) .
Większość z nich jest niepraktykująca .
Posługują się językiem szwedzkim .
W etnogenezie Szwedów kluczową rolę odegrały germańskie ludy Swewów i Gotów .
Swój udział mieli też Finowie i Lapończycy .
