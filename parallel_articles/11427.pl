Język szwedzki
Język szwedzki ( szw .
, ) - język z grupy skandynawskiej języków germańskich , ojczysty dla ponad 10 milionów osób , głównie używany Szwecji i niektórych części Finlandii .
Jest także językiem urzędowym na terenie tych państw ( w Finlandii obok języka fińskiego ) .
Podobny do języka norweskiego , w mniejszym stopniu do języka duńskiego .
Jest częściowo rozumiany przez osoby biegle posługujące się tymi językami .
Dialekty tych trzech języków tworzą ze sobą kontinuum dialektalne .
Język standardowy w Szwecji , tzw .
rikssvenska ( szwedzki państwowy ) , opiera się na dialektach regionu sztokholmskiego .
W sytuacjach nieformalnych powszechnie używane są zróżnicowane dialekty nieliterackie .
Większość z nich jest jednak dobrze zrozumiała dla pozostałych użytkowników języka szwedzkiego .
Obecnie można wyróżnić dwa zespoły dialektów : szwedzki i finlandzki ( tzw .
fińska odmiana języka szwedzkiego ) .
Istnieje też zespół dialektów języka szwedzkiego z Estonii , jednak jego znaczenie zmniejszyło się , po zajęciu Estonii przez Armię Czerwoną , kiedy to większość Estońskich Szwedów opuściło ją .
W południowej części Szwecji – Skanii ( Skåne ) – język mówiony uległ silnym wpływom duńskim , z bardzo gardłową wymową .
Mieszkańcy Wysp Alandzkich mówią dialektem zbliżonym do północno sztokholmskiego .
Większość zapożyczeń w języku szwedzkim pochodzi z języków : niemieckiego , francuskiego , angielskiego , łaciny i greki .
Charakterystyczną cechą języka szwedzkiego jest obfitość w samogłoski , posiadające 9 fonemów podstawowych , a wliczając w nie rozróżnienie między długimi i krótkimi samogłoskami – 17 fonemów samogłoskowych .
Warianty Szwedzkiej spółgłoski `` sj '' ( sj-ljudet ) – w zależności od dialektu – odpowiadają w przybliżeniu głoskom `` h '' , `` sz '' lub `` ś '' .
W wersji podstawowej nie ma odpowiednika w innych językach ( mimo że w podobny sposób wymawia się ją w języku holenderskim ) .
Historia .
Pierwsze ślady języka północnogermańskiego pochodzą z III wieku .
Około IX wieku , w okresie wikińskim , pojawił się `` norrønt '' , a potem `` dansk tunga '' , czyli niezróżnicowane germańskie języki Północy .
Następnie doszło do rozszczepienia na języki północnogermańskie wschodnie i północnogermańskie zachodnie .
