Szymbark ( województwo warmińsko-mazurskie )
Szymbark ( niem .
`` Schönberg '' ) – osada w Polsce położona w województwie warmińsko-mazurskim , w powiecie iławskim , w gminie Iława , nad Jeziorem Szymbarskim .
Wieś wymieniona została po raz pierwszy wymieniona w 1378 jako osada przyzamkowa .
W latach 1975–1998 miejscowość położona była w województwie olsztyńskim .
Pomniki przyrody .
W pobliżu ruin zamku ( bliżej drogi Iława-Susz ) rośnie ponad 210-letni jesion o obwodzie 325 cm i wysokości 40 m oraz 200-letnie sosny .
Od strony wsi Kamionka do zamku prowadzi aleja starych dębów .
