Szymkent
Szymkent ( kaz .
Шымкент ; ros .
Шымкент , `` Szymkient '' , dawniej Чимкент , `` Czimkient '' ; również Symkent , Czymkent ) – miasto leżące na południowych krańcach Kazachstanu , przy granicy z Uzbekistanem .
W mieście rozwinęło się górnictwo i hutnictwo ołowiu , przemysł maszynowy , chemiczny , mineralny , włókienniczy , skórzany ( karakuły ) , spożywczy .
Rafineria ropy naftowej ; węzeł kolejowy ; szkoły wyższe , teatry , muzeum .
Przez miasto przebiega droga międzynarodowa M32 prowadząca do Taszkentu .
1 005 996 mieszkańców ( stan na 01 lipca 2018 ) .
W mieście znajduje się Port lotniczy Szymkent .
Ma tu również siedzibę klub piłkarski Ordabasy Szymkent .
