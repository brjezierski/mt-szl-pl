Szerokość geograficzna ( symbol φ )
Wartości szerokości geograficznej rozciągają się od 0° na równiku do 90° na biegunach .
Szerokość geograficzna może być północna ( N ; zobacz : półkula północna ) lub południowa ( S ; zobacz : półkula południowa ) .
Dla kierunku północnego oznacza się ją znakiem dodatnim ( lub N ) , dla kierunku południowego ujemnym ( lub S ) , czyli odpowiednio np .
: +90° = 90°N oraz –90° = 90°S .
Zbiór punktów na kuli ziemskiej o tej samej szerokości geograficznej to równoleżnik .
Jednostkami szerokości geograficznej ( kąt ) są : stopień [ ° ] , minuta [ ' ] i sekunda kątowa [ `` ] .
Szerokość geograficzną w istocie mierzy się bezpośrednio na sferze niebieskiej , gdzie stanowi kąt pomiędzy kierunkiem na zenit a płaszczyzną równika niebieskiego .
Szerokością geograficzną jest również wysokość nad horyzontem północnego bieguna niebieskiego .
