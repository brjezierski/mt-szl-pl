Szyndzielnia
Szyndzielnia ( dawniej również `` Szędzielnia '' lub `` Gaciok '' ; niem .
`` Kamitzerplatte '' – od nazwy leżącej u podnóża góry Kamienicy , spotykane również `` Kamnitzerplatte '' , 1030 m n.p.m . )
– szczyt w Beskidzie Śląskim , wznoszący się tuż na północ od Klimczoka , usytuowany pomiędzy dolinami Olszówki , Wapienicy i Białki w granicach administracyjnych miasta Bielska-Białej .
Mapy i przewodniki podają wysokość 1028 ( zob .
np . ) .
Przyczyną nieścisłości jest punkt pomiarowy na wysokości 1028,3 , który jednak leży nieco poniżej szczytu .
Masyw Szyndzielni porośnięty jest lasami bukowymi z domieszką drzew iglastych – świerków i jodeł .
