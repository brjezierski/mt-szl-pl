Szczypiorek
Czosnek szczypiorek , nazywany w skrócie szczypiorkiem ( `` Allium schoenoprasum '' L. ) – gatunek rośliny z rodziny czosnkowatych .
W stanie dzikim jest szeroko rozprzestrzeniony na kuli ziemskiej .
Występuje w Europie , Azji i Ameryce Północnej .
Jest też powszechnie uprawiany w wielu krajach świata .
Status gatunku we florze Polski : roślina uprawna , efemerofit .
Uprawa .
Rozmnaża się z nasion lub przez podział kępek .
Nadaje się do uprawy w ogrodzie , w skrzynkach i doniczkach .
Co 2-3 lata należy kępę szczypiorku wykopać i rozdzielić cebulki , gdyż ulega ona nadmiernemu zagęszczeniu .
