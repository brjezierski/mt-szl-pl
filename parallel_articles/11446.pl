Szumawa ( makroregion )
Szumawa ( cz .
`` Šumavská hornatina '' , niem .
`` Bayerischer Wald '' lub `` Böhmerwald '' ) – makroregion fizycznogeograficzny na terenie Czech , Bawarii i Austrii , stanowiący południową część Krainy Szumawskiej , ciągu pasm górskich i wyżyn ograniczających Masyw Czeski od południowego zachodu .
Nazewnictwo .
Nazewnictwo regionu różni się dość wyraźnie w podejściu czeskim , niemieckim i austriackim .
W popularnej w Polsce regionalizacji fizycznogeograficznej Europy J. Kondrackiego Szumawą określa się cały makroregion , natomiast w geografii czeskiej jedynie mezoregion stanowiący jego najwyższą część , zaś makroregion jest określany jako `` Šumavská hornatina '' .
W Geografii niemieckiej używa się określenia Las Bawarski ( niem .
`` Bayerischer Wald '' ) , ale wyłącznie w odniesieniu do części bawarskiej i jest to też wariantowy egzonim używany w języku polskim .
W Austrii tego makroregionu nie wyróżnia się wcale i stanowi on w tamtejszym ujęciu część tzw .
Płyty Granitowo-Gnejsowej ( niem .
`` Granit- und Gneisplateau '' ) .
Wyróżnia się jednak Szumawę właściwą ( jako `` Böhmerwald '' ) .
Do Szumawy zaliczają się austriackie regiony Mühlviertel w Górnej Austrii i zachodnia część regionu Waldviertel w Dolnej Austrii .
Geografia .
Makroregion Szumawy obejmuje południowo-zachodni skraj Czech , oraz fragment wschodniej Bawarii i północnej Austrii .
Jest to długi na 180 km i szeroki na około 90 km pas gór i pogórzy ciągnący się wzdłuż granicy Czech , Bawarii i Austrii , ograniczony od południa i południowego zachodu Dunajem między Ratyzboną , Linz i Krems .
Rzeka stanowi w przybliżeniu granicę między Pozaalpejską Europą Środkową i Hercynidami na północy , a Wyżyną Bawarską ( niem .
`` Alpenvorland '' ) na południu .
Po stronie czeskiej i austriackiej na wschodzie obszar przechodzi płynnie w Wyżynę Czesko-Morawską .
Na północy Wyżyna Všerubska ( cz .
`` Všerubská vrchovina '' , niem .
`` Cham-Further Senke '' ) oddziela Szumawę od Lasu Czeskiego , a na zachodzie Szumawa graniczy z Basenem Szwabsko-Frankońskim .
Jest to obszar silnie zalesiony i stosunkowo słabo zaludniony , szczególnie po stronie czeskiej .
