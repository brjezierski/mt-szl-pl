Sátoraljaújhely
Sátoraljaújhely ( słow .
`` Nové Mesto pod Šiatrom '' , niem .
`` Neustadt am Zeltberg '' ) – miasto powiatowe w północno-wschodnich Węgrzech , w komitacie Borsod-Abaúj-Zemplén .
Położenie .
Sátoraljaújhely leży nad rzeką Roňavą , w dolinie Bodrogu , u południowych podnóży Gór Tokajsko-Slańskich , na północnej granicy regionu Bodrogköz .
