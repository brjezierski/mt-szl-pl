São Gonçalo
São Gonçalo – miasto w południowo-wschodniej Brazylii ( stan Rio de Janeiro ) , w regionie metropolitalnym Rio de Janeiro-Niterói .
W 2020 roku miasto liczyło 1,09 mln mieszkańców
W mieście rozwinął się przemysł cementowy , metalowy , chemiczny i spożywczy .
