São José dos Campos ( wym .
port .
[ ] ) – miasto w południowo-wschodniej Brazylii ( stan São Paulo ) , nad rzeką Paraiba .
Liczba mieszkańców miasta w 2003 roku wynosiła ok. 595 tys. , a zespołu miejskiego ok. 841 tys .
Należy do mezoregionu Paulista Paraiba Valley i mikroregionu São José dos Campos .
Jest to siedziba Metropolitan Region Paraíba i North Coast Valley , położona na wschód od stolicy stanu , w odległości 94 km .
Zajmuje powierzchnię 1100 km² , z czego 353,9 km² znajdują się w obszarze miejskim .
W 2016 roku jej populacja została oszacowana przez IBGE na 695 992 mieszkańców , i jest drugim najbardziej zaludnionym miastem w Brazylii , za Campinas .
