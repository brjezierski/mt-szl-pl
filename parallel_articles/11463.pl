Sócrates
Sócrates ( wł .
Sócrates Brasileiro Sampaio de Souza Vieira de Oliveira , ur .
19 lutego 1954 w Ribeirão Preto , zm .
4 grudnia 2011 w São Paulo ) – jeden z najwybitniejszych brazylijskich piłkarzy .
Życiorys .
Jego umiejętności w zakresie czytania gry oraz prowadzenia piłki były oceniane jako klasa światowa .
Znakiem firmowym Sócratesa były zagrania piętką .
Był lekarzem pediatrą ( po zakończeniu kariery zawodniczej prowadził praktykę lekarską w Ribeirão Preto ) , intelektualistą , ale również nałogowym palaczem i człowiekiem niestroniącym od alkoholu .
Jako jeden z najlepszych pomocników w historii futbolu Sócrates grał w reprezentacji Brazylii i był jej kapitanem podczas Mistrzostw Świata 1982 .
Brał także udział w zawodach w 1986 .
Sócrates rozpoczynał swoją profesjonalną karierę w 1974 roku w klubie Botafogo Ribeirão Preto w stanie São Paulo , skąd w 1978 roku przeszedł do Corinthians Paulista São Paulo .
Występował również we włoskiej Fiorentinie oraz brazylijskich CR Flamengo i Santos FC , zmierzając do końca swojej kariery .
W 2004 roku , po ponad 10 latach piłkarskiej emerytury , zgodził się na miesięczny kontrakt na posadzie grającego trenera amatorskiego klubu Garforth Town z Anglii .
Został wybrany przez FIFA jednym ze stu najlepszych piłkarzy XX wieku ( lista FIFA 100 ) .
Zmarł w wieku 57 lat w szpitalu w São Paulo wskutek wstrząsu septycznego .
Jego brat Raí również był piłkarzem , mistrzem świata z 1994 roku .
