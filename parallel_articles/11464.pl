Sölden ( Badenia-Wirtembergia )
Sölden ( alem .
`` Sailede '' ) – miejscowość i gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Fryburg , w regionie Südlicher Oberrhein , w powiecie Breisgau-Hochschwarzwald , wchodzi w skład związku gmin Verwaltungsgemeinschaft Hexental .
Leży ok. 10 km na południe od Fryburga Bryzgowijskiego .
