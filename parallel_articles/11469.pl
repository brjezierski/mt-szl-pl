Sławniowice
Sławniowice ( ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Głuchołazy .
Historycznie leży na Dolnym Śląsku , na ziemi nyskiej .
Położona jest na terenie Przedgórza Paczkowskiego , będącego częścią Przedgórza Sudeckiego .
Przepływa przez nią rzeka Mora .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 548 osób .
