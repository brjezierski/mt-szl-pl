Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 1 km od granicy z Czechami , we wschodniej części Przedgórza Paczkowskiego .
Należy do Euroregionu Pradziad .
Przez granice administracyjne wsi przepływa rzeka Mora .
