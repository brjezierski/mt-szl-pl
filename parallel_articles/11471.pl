Słoków
Słoków ( ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Lubrza .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 31 grudnia 2013 r. wieś była zamieszkana przez 91 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 3 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy gminy Lubrza z gminą Biała .
Należy do Euroregionu Pradziad .
Ogólna powierzchnia wsi to 248 ha , w tym użytków rolnych 225 ha .
Środowisko naturalne .
W Słokowie panuje klimat umiarkowany ciepły .
Średnia temperatura roczna wynosi +8,1 °C .
Duże zróżnicowanie dotyczy termicznych pór roku .
