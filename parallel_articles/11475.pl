Językiem tym posługuje się ponad 6 mln osób – przede wszystkim na Słowacji , gdzie jest językiem urzędowym .
Dodatkowo język słowacki jest jednym z języków urzędowych w serbskiej Wojwodinie .
Używają go także Słowacy mieszkający w Polsce , Rumunii , na Węgrzech , w USA i Kanadzie .
W Polsce była możliwość zdawania matury z języka słowackiego .
