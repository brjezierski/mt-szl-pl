Słowenia
Słowenia , Republika Słowenii ( ) – państwo położone w Europie Środkowej nad morzem Adriatyckim , graniczy od zachodu z Włochami , od północy z Austrią , od wschodu z Węgrami oraz od południa z Chorwacją .
Stolicą i największym miastem kraju jest Lublana .
Słowenia jest najzamożniejszym oraz najbardziej rozwiniętym , szczególnie pod względem gospodarczym , państwem byłej Jugosławii i jednocześnie jednym z najbardziej zamożnych i rozwiniętych krajów Europy Środkowo-Wschodniej .
Językiem urzędowym jest język słoweński .
Słowenia należy do ONZ od 1992 roku , NATO od 29 marca 2004 , jest też członkiem UE od 1 maja 2004 roku , od 1 stycznia 2007 kraj jest również członkiem strefy euro .
