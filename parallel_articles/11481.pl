Język słoweński
Język słoweński ( słoweń .
lub ) – język południowosłowiański , urzędowy w Słowenii i jeden z oficjalnych języków Unii Europejskiej .
Posługuje się nim ok. 2,4 mln osób , z czego większość stanowią mieszkańcy Słowenii , a także terenów przygranicznych w Austrii i we Włoszech .
Klasyfikacja .
Podobnie jak serbsko-chorwacki , słoweński to język indoeuropejski należący do zachodniej gałęzi grupy południowosłowiańskiej .
