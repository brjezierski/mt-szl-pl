Słupice ( gromada )
Słupice – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Słupice z siedzibą GRN w Słupicach utworzono 1 stycznia 1960 w powiecie dzierżoniowskim w woj .
wrocławskim z obszarów zniesionych gromad Jaźwina i Oleszna ( bez wsi Piotrówek ) w tymże powiecie .
Dla gromady ustalono 22 członków gromadzkiej rady narodowej .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
