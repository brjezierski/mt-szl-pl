Dzieciństwo i młodość .
Vespucci urodził się 9 marca 1451 roku we Florencji .
Od najmłodszych lat marzył o dalekich podróżach .
Zanim jednak je rozpoczął , zdobył bardzo dobre wykształcenie .
W nauce wspierał go wuj .
Amerigo był znakomitym matematykiem .
Jego ulubionym zajęciem było kopiowanie map .
Po ukończeniu nauki Vespucci jako młodzieniec został kupcem .
Był agentem domu bankowego należącego do sławnego , potężnego rodu Medyceuszy z Florencji .
Vespucci w Sewilli .
W 1492 roku Amerigo Vespucci opuścił rodzinną Florencję i udał się do Sewilli w Hiszpanii .
Wysłał go tam sam Medyceusz , gdyż w Hiszpanii kwitł handel , a to między innymi dzięki dalekomorskim podróżom Hiszpanów i żeglarzy , którzy pracowali dla hiszpańskich władców .
Jednym z poważniejszych zadań kupieckich Vespucciego w Sewilli było wyposażenie we wszystko , co niezbędne do drugiej i trzeciej wyprawy Kolumba .
Wyprawy .
Według własnego świadectwa został wybrany przez króla Manuela I miał jakoby w latach 1497–1504 odbyć cztery wyprawy odkrywcze , w co powątpiewają dzisiejsi badacze .
Dotarłszy w sierpniu 1500 roku nad jezioro Maracaibo , żeglarze zobaczyli Indian żyjących w palafitach , czyli chatach zbudowanych na palach wbitych w dno akwenu .
Amerigo Vespucci w liście do jednego ze swych przyjaciół we Florencji nazwał to miejsce `` Małą Wenecją '' ( wł .
`` Venezuela '' ) – Wenezuelą .
Vespucci pracował na usługach Hiszpanów i Portugalczyków .
W 1500 przyłączył się do wyprawy Vicenta Pinzona , podczas której odkrył ujście Amazonki i zbadał wybrzeże Brazylii do 6° szerokości geograficznej południowej .
Pewne jest , że w latach 1500–1501 uczestniczył – jako przyrodnik i kartograf – w hiszpańskiej ekspedycji żeglarzy Alonso de Hojeda i Juana de Cosa .
Dopłynęli oni do półwyspu Guajira , znajdującego się w zachodnim krańcu obecnej Wenezueli .
W czasie jej trwania już samodzielnie dotarł do Amazonki , zbadał wybrzeże Brazylii , dopłynął do ujścia rzeki Orinoko i zawrócił do Kadyksu .
W latach 1501–1502 na okręcie portugalskim opłynął brzegi Brazylii .
Od 1503 zaczęły się ukazywać drukiem listy Vespucciego wraz z barwnymi opisami poznanych krajów .
Tłumaczone na wiele języków , zyskały mu tak dużą popularność , iż powszechnie jego , a nie Kolumba , zaczęto uważać za odkrywcę Ameryki , a nowy ląd nazwano od jego imienia .
Vespucci pracował też dla króla Portugalii .
W latach 1501–1502 pełnił funkcję kosmografa podczas portugalskiej wyprawy do wschodnich wybrzeży Ameryki Południowej , od ujścia Magdaleny do Bahía Blanca .
Zbadał znaczną część wschodniego wybrzeża Ameryki Południowej .
Opisał odkrycie rzek Rio de Janeiro i Rio de la Plata oraz Patagonii i Georgii Południowej .
To właśnie podczas tej podróży Amerigo – w odróżnieniu od Krzysztofa Kolumba , który sądził , że dotarł do Indii – uznał odwiedzone tereny za nieznany wcześniej Europejczykom kontynent .
Nie jest wiadome , ile razy Vespucci przemierzał Atlantyk , nawet jego własne relacje są niejednoznaczne .
Relacje z jego wypraw zawarte w listach do przyjaciół różniły się między sobą , a dane co do długości i szerokości geograficznych nie zawsze były zgodne z prawdą .
W pierwszą podróż udał się prawdopodobnie w 1497 roku .
Vespucci twierdził później , że to właśnie wtedy , rok przed Kolumbem , ujrzał Amerykę Południową .
Zmarł 22 lutego 1512 roku w Sewilli .
