Słonin
Słonin – wieś w Polsce położona w województwie wielkopolskim , w powiecie kościańskim , w gminie Czempiń .
Wieś położona była w 1581 roku w powiecie kościańskim województwa poznańskiego .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako Słonin należała do wsi większych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Słonin należał do okręgu kościańskiego tego powiatu i stanowił część majątku Racat ( niem .
Razoten , dziś Racot ) , który należał wówczas do byłego króla Niderlandów ( Wilhelma I ) .
Według spisu urzędowego z 1837 roku Słonin liczył 238 mieszkańców , którzy zamieszkiwali 28 dymów ( domostw ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
Na terenie wsi znajdują się dwa obiekty zabytkowe : szkoła murowana z 1882 i oberża ( dom nr 49 ) , szachulcowa z końca XIX wieku .
