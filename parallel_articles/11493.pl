Śniatyn
Śniatyn ( ukr .
Снятин , `` Sniatyn '' ) – miasto na Ukrainie , w obwodzie iwanofrankiwskim , nad Prutem , siedziba rejonu śniatyńskiego .
Miasto królewskie lokowane w 1411 roku położone było w XVI wieku w województwie ruskim .
Śniatyn uzyskał prawo składu w 1635 roku .
Historia .
Miejscowość była wzmiankowana w 1158 roku .
W 1415 roku w Śniatynie przebywał król Władysław Jagiełło , który przyjął tu hołd wojewody mołdawskiego Aleksandra .
W 1448 roku Kazimierz IV Jagiellończyk obdarował Śniatyn miejskim prawem niemieckim .
