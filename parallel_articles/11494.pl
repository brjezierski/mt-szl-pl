Snihuriwka
Snihuriwka ( ukr .
`` Снігурівка '' ) – miasto na Ukrainie w obwodzie mikołajowskim .
Do 17 lipca 2020 centrum administracyjne zlikwidowanego rejonu snihuriwskiego .
Węzeł kolejowy .
Ośrodek przemysłu spożywczego .
Historia .
Jesienią 1812 osadnicy z obwodu mohylewskiego przybyli do guberni chersońskiej .
Na prawym brzegu rzeki Ingulec ( dopływu Dniepru ) założyli wioskę .
