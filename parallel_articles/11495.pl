Sumer
Sumer ( `` Szumer '' , `` Sumeria '' ; sum .
𒌦 𒊕 𒈪 𒂵 `` Ki-en-gir '' , hebr .
שִׁנְעָר `` Szinear '' – por .
Rdz 10:10 ) – starożytna kraina leżąca w południowej części Mezopotamii ( dzisiaj południowy Irak ) .
Zamieszkiwali ją Sumerowie , którzy stworzyli wysoko rozwiniętą cywilizację uznawaną za kolebkę kultur Bliskiego Wschodu i Europy .
Wybudowali na terenie Mezopotamii szereg miast , z których największe to : Eridu , Ur , Lagasz , Umma , Uruk , Kisz , Sippar .
Sumerowie prowadzili osiadły tryb życia .
Trudnili się głównie hodowlą , rolnictwem i rzemiosłem .
Nie utworzyli jednak scentralizowanego państwa , a jedynie system miast-państw .
Każde z nich posiadało swojego króla , zarazem najwyższego kapłana , w którego rękach spoczywała władza religijna , administracyjna , jak również militarna .
Historia Sumeru .
Pochodzenie Sumerów stanowi dla nauki zagadkę .
Jedynie nieliczne dane umożliwiają wnioskowanie w tej kwestii .
Wedle podań sumeryjskich twórcy cywilizacji mezopotamskiej przybyli drogą morską do południowego Dwurzecza i osiedlili się nad Zatoką Perską .
Możliwe , że przybyli tu z gór Zagros .
Część językoznawców skłania się nawet widzieć kolebkę Sumerów w Tybecie , Azji Południowo-Wschodniej lub nawet na Malajach , udowadniając rzekome pokrewieństwo języka Sumerów z językami ludów dziś zamieszkujących te obszary .
Język Sumerów nie wykazuje jednak tak naprawdę żadnych podobieństw z innymi językami .
Jego geneza ( podobnie jak całego ludu ) jest całkowicie nieznana .
Mimo tych niewiadomych można jednak postawić tezę , iż twórcami kultury Ubajd , Uruk i pierwszymi mieszkańcami dolnego biegu Tygrysu i Eufratu byli przynajmniej po części Sumerowie .
Ostatnio coraz częściej wiąże się genezę państwa Sumerów z kulturą Indusu .
Oprócz antropologii Sumerów nieznana jest również ich asymilacja z pierwotnymi Semitami .
Najprawdopodobniej przybysze pokojowo zasymilowali się z gospodarzami , a dzięki znacznie wyższemu stopniowi kultury , łatwo osiągnęli przewagę w dolnej Mezopotamii ( zwanej przez Semitów Sumerem ) .
