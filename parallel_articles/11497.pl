Somaliland
Somaliland ( Republika Somalilandu , , , ) – islamska republika , nieuznawana przez społeczność międzynarodową , leżąca we wschodniej Afryce nad Zatoką Adeńską .
Somaliland ogłosił secesję od Somalii w 1991 roku i od tamtej pory zdołał zbudować funkcjonujący organizm państwowy z demokratycznie wybieranym prezydentem i parlamentem .
Graniczy z Dżibuti , Etiopią i Puntlandem – inną częścią Somalii .
Geografia .
Somaliland zajmuje tereny dawnego Somali Brytyjskiego .
Położony jest w zasięgu ciepłego klimatu pustynnego ( BWh ) .
Północne niziny położone są w sąsiedztwie Zatoki Adeńskiej .
Na północnym wschodzie wzdłuż wybrzeża ciągnie się pasmo górskie Cal Madow , którego najwyższy szczyt to Shimbiris ( 2407 m n.p.m . ) .
