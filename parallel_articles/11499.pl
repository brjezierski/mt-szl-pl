Sombrero
Sombrero ( hisz .
`` sombrero '' kapelusz ) to wysoki kapelusz słomiany lub filcowy z miękkim , szerokim rondem .
Był używany w Hiszpanii w XV i XVI wieku .
Stamtąd trafił do hiszpańskich kolonii w Ameryce Środkowej i Południowej .
Stanowi narodowe nakrycie głowy Meksykanów i Chilijczyków .
Jedna z galaktyk z racji swojego kształtu została nazwana Galaktyką Sombrero .
