Somonino ( stacja kolejowa )
Somonino – stacja kolejowa w Somoninie , w gminie Somonino , w województwie pomorskim .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Przez stację przebiegają linie kolejowe nr 201 i 214 .
Na stacji zatrzymują się pociągi kursujące pomiędzy Kościerzyną , a Gdynią Główną .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
Stacja posiada dwa perony , przy których znajdują się trzy krawędzie peronowe .
W budynku dworcowym działa kasa biletowa .
Za stacją w kierunku Gdyni tory rozwidlają się w kierunku Kartuz .
W 2010 cześć semaforów kształtowych została zastąpiona semaforami świetlnymi .
W 2014 Stacja przeszła modernizację , która objęła wymianę nawierzchni torów nr 1 i 2 , wymianę rozjazdów oraz budowę nowego peronu .
Budynek dworca w roku 2015 został przekazany samorządowi gminnemu .
Linie kolejowe .
Somonino jest węzłem kolejowym , w którym krzyżują się dwie linie kolejowe : linia 201 Nowa Wieś Wielka – Gdynia Port Centralny i czynna jedynie dla ruchu towarowego linia 214 Somonino – Kartuzy .
Po zamknięciu dla ruchu pasażerskiego linii Somonino – Kartuzy , stacja Somonino jest wykorzystywane głównie jako mijanka .
