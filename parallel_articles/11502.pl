Sągole
Sągole – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Kosów Lacki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Mieszkańcy wyznania rzymskokatolickiego należą do parafii św .
Wojciecha w Skibniewie-Podawcach .
Wieś Sągole powstała wskutek wydzielenia się części gruntów należących do wsi Kurcze na początku XVI wieku .
W roku 1525 w aktach sądowych drohickich pojawia się Stanisław „ Sągol ” ( Stanislao Zagol ) z Kurczów .
Wyrokiem sądu Stanisław Dybowski miał być wprowadzony w swe dobra , do których go nie dopuszczał Stanisław „ Zagol ” z Kurczów , co miał wyegzekwować woźny Piotr Trzciński .
Od jego to przydomku wziął nazwę przysiółek Sągole ( = Sągolowie ) powstały na gruntach wsi Kurcze .
W 1561 roku w dokumentach pojawia się określenie „ `` grunty Sągolów '' ” .
