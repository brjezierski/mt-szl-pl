TVS
TVS ( `` Telewizja Silesia '' ) – pierwsza polskojęzyczna regionalna stacja telewizyjna o profilu informacyjno-rozrywkowym .
Kanał TVS jest kierowany głównie do mieszkańców województwa śląskiego , obejmującego historyczne ziemie Górnego Śląska i Małopolski ( w tym Zagłębie Dąbrowskie i Zagłębie Krakowskie ) .
TV Silesia został stworzony jako konkurencja dla TVP Katowice .
Historia .
Lata 2006–2007 .
Pod koniec 2006 roku został złożony wniosek do KRRiT o przyznanie koncesji kanału .
Rok 2008 .
19 marca 2008 roku kanał regionalny TVS zainaugurował emisję testową .
27 marca 2008 roku wystartował portal internetowy TVS .
Na stronie tvs.pl są publikowane informacje z województwa śląskiego wraz ze zdjęciami i materiałami wideo , pojawiają się też artykuły z różnych dziedzin życia .
29 marca 2008 roku po godz .
10:00 widzów TVS przywitał Marek Czyż w towarzystwie regionalnego Zespołu Pieśni i Tańca „ Śląsk ” .
Arkadiusza Hołdę , prezesa TV Silesia powitano chlebem i solą .
Podczas pierwszych godzin dziennikarze kanału rozmawiali z zaproszonymi gośćmi , m.in .
Arturem Rojkiem , Kamilem Durczokiem , Tadeuszem Sławkiem i Krystyną Bochenek .
14 kwietnia 2008 roku o godz .
21:00 na antenie zadebiutował magazyn informacyjny „ Silesia informacje ” .
5 maja 2008 roku TV Silesia zdecydowała o wydłużeniu czasu emisji programu oraz zwiększeniu liczby programów informacyjnych i publicystycznych .
5 lipca 2008 roku o godz .
7:00 zadebiutowało „ Radio Silesia na wizji ” , pierwszy tego typu program w Polsce .
Od 4 grudnia 2008 roku stacja jest dostępna dla Polonii w USA i Kanadzie .
Rozpoczęto nadawanie programu przez całą dobę .
W czasie świąt Bożego Narodzenia w roku 2008 Telewizja Śląska pokazała pierwsze filmowe pozycje .
31 grudnia 2008 roku stacja zorganizowała pierwszą imprezę sylwestrową w Katowicach .
Rok 2009 .
Po Nowym Roku zdecydowano na regularną emisję filmów .
W marcu 2009 roku wprowadzono teletext .
29 marca 2009 roku TVS przygotowała dla widzów „ Dzień Otwarty TVS ” z występami gwiazd śląskiej estrady .
W październiku nowym dyrektorem programowym stacji zostaje Marek Czyż .
Zastępuje na tym stanowisku Sławomira Zielińskiego , który dwa miesiące wcześniej opuścił stacje .
Na razie nie wiadomo , kto zastąpi twarz TVS na antenie .
Emisja .
Profil .
Program informuje głównie o wydarzeniach z regionu .
Dodatkowo emitowany jest pasek informacyjny u dołu ekranu z ważniejszymi informacjami .
W TVS zobaczyć można również programy promujące Śląsk , jego kulturę , historię , muzykę i tradycje .
Ma poruszać tematy związane z regionem i województwem śląskim .
Program jest nadawany przez 24 godziny na dobę .
Uruchomienie kosztowało ok. 20 mln zł .
Logotyp .
Logo TVS nadawane od 2 marca 2008 : trzy zaokrąglone kwadraty w poziomie , dwa pierwsze koloru niebieskiego z białymi literami `` T V '' , trzeci koloru czerwonego z literą `` S '' .
Do loga w miarę potrzeb są dodawane napisy : Na żywo , Powtórka lub Emisja testowa .
Firmowe logo ma pod spodem napis : Telewizja Silesia .
Logo kanału mieści się u góry po prawej stronie ekranu .
Odbiór .
Stacja jest dostępna w niektórych sieciach kablowych w regionie , poprzez niekodowany przekaz satelitarny ma zasięg europejski .
Kanał jest dostępny we wszystkich platformach cyfrowych , wybranych sieciach kablowych i niekodowanym przekazie satelitarnym FTA .
Program jest produkowany w technologii HDTV , jednak w rozdzielczości HD jest dostępny jedynie w cyfrowych kablówkach .
Na potrzeby przekazu satelitarnego i do analogowych telewizji kablowych sygnał jest konwertowany do standardowej rozdzielczości SD w proporcjach 16:9 anamorficznie .
Redakcja .
Siedziba główna znajduje się w Katowicach przy Placu Grunwaldzkim .
Zespół TVS liczy około 110 osób .
Twarzą kanału jest Marek Czyż , który do sierpnia 2016 pracował w Telewizji Polskiej .
Radio Silesia .
TV Silesia nadaje nie tylko telewizję , ale także dwa programy radiowe : Radio Silesia ( nRadio 235 ) i Radio Silesia Club ( nRadio 236 ) .
Obydwa radia są dostępne w nRadiu i w internecie .
Nadają 24 godziny na dobę .
Radio Silesia emitowane było również na antenie TV Silesia od 23:45 do 7:00 .
Główny kanał emituje popularną muzykę śląską , pieśni biesiadne i inne , za to Radio Silesia Club jak sama nazwa , klubową .
Od soboty 5 lipca 2008 roku , Radio Silesia w godzinach 7:00 do 10:00 jest dostępne w internecie oraz na antenie TVS nadaje na wizji .
Prowadzącymi są prezenterzy TVS : Patrycja Tomaszczyk , Nina Nocoń , Anna Piątek , Agnieszka Wajs , Adam Giza , Paweł Zięba , Grzegorz Stasiak i Michał Wasik .
