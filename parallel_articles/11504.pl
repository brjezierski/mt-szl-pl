Telewizja TVN nadal jest zainteresowana śląską telewizją TV Silesia .
W ramówce telewizji pojawiły się produkcje TVN-u .
TVS emituje m.in .
program „ Nauka jazdy ” , „ Usterka ” , „ Maraton Uśmiechu ” , „ Cela ” .
Jak ustalił Presserwis , śląska telewizja dzieli się zyskami z reklam z TVN-em .
Prawdopodobnie w niedługim czasie na antenie stacji pojawią się kolejne programy TVN-u .
Kanał nadaje w każdy piątek „ Ligę hitów Radia Zet ” .
Program tak samo , jak i w radiu prowadzi Tomasz Miara , który w telewizji prezentuje 15 przebojów , wybieranych w minionym tygodniu przez słuchaczy Radia Zet na stronie internetowej www.radiozet.pl .
25 grudnia stacja wyemituje 1 odcinek programu detektywistycznego „ Rutkowski Patrol ” ukazującego pracę agencji detektywistycznej Krzysztofa Rutkowskiego .
Program realizuje katowicka firma Grupa Nonsens , której przedstawiciele poinformowali , że serial tworzony jest w jakości Full HD , czasem z ukrytych kamer .
