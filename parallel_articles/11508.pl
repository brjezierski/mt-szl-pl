Młodość .
Ukończył w 1946 Liceum Ogólnokształcące im .
Marszałka Stanisława Małachowskiego w Płocku , następnie rozpoczął ( ostatecznie nieukończone ) studia prawnicze na Uniwersytecie Warszawskim , w trakcie których był członkiem „ Caritas Academica ” , a w latach 1947–1948 przewodniczył Akademickiej Spółdzielni Wydawniczej .
W 1946 wstąpił do Stronnictwa Pracy , jednak wkrótce odszedł z tej partii .
Działalność w Polsce Ludowej .
Stowarzyszenie „ Pax ” .
W latach 1948–1955 Tadeusz Mazowiecki działał w Stowarzyszeniu „ Pax ” .
Pod koniec lat 40. uczestniczył w wewnętrznych dyskusjach programowych „ Paxu ” i zgłaszał wątpliwości wobec jednoznacznego opowiedzenia się za ustrojem socjalistycznym .
Początkowo ( do 1951 ) był w grupie tygodnika „ Dziś i Jutro ” , następnie ( 1950–1952 ) pełnił funkcję zastępcy redaktora naczelnego dziennika „ Słowo Powszechne ” .
W 1952 należał do grupy młodych działaczy ( m.in .
z Januszem Zabłockim ) , którzy sprowokowali dyskusję wewnętrzną , która dała zaczyn tzw .
