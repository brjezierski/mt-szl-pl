Tadeusz Mazowiecki
( ur .
18 kwietnia 1927 w Płocku , zm .
28 października 2013 w Warszawie ) – polski polityk i publicysta .
Ostatni prezes Rady Ministrów PRL i pierwszy III Rzeczypospolitej ( w latach 1989–1991 ) , współtwórca i przewodniczący Unii Demokratycznej i Unii Wolności .
Kawaler Orderu Orła Białego .
Do połowy lat 50. działacz Stowarzyszenia „ Pax ” , poseł na Sejm PRL III , IV i V kadencji ( w latach 1961–1972 ) oraz na Sejm RP I , II i III kadencji ( w latach 1991–2001 ) ; w latach 1991–1995 specjalny wysłannik ONZ w Bośni i Hercegowinie , w latach 2010–2013 doradca prezydenta RP Bronisława Komorowskiego ds .
polityki krajowej i międzynarodowej .
