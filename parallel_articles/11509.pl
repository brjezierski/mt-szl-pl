Tadeusz Rydzyk
Tadeusz Rydzyk CSsR ( ur .
3 maja 1945 w Olkuszu ) – polski duchowny rzymskokatolicki , redemptorysta , doktor nauk teologicznych , nauczyciel akademicki .
Założyciel i dyrektor rozgłośni radiowej Radio Maryja oraz Telewizji Trwam , współzałożyciel i prezes zarządu Fundacji Lux Veritatis oraz założyciel i były rektor Wyższej Szkoły Kultury Społecznej i Medialnej w Toruniu , w której jest przewodniczącym Rady Naukowej i wykładowcą .
Życiorys .
Syn Stanisławy Rydzyk z domu Piątek i jej nieformalnego partnera Bronisława Kordaszewskiego .
Otrzymał nazwisko matki , wdowy po Franciszku Rydzyku , zamordowanym przez Niemców w 1941 .
Wczesne dzieciństwo spędził w rodzinnym Olkuszu .
Uczył się w Wyższym Seminarium Duchownym Redemptorystów w Tuchowie , następnie studiował teologię biblijną na Akademii Teologii Katolickiej w Warszawie .
W 1971 złożył wieczyste śluby zakonne oraz przyjął święcenia kapłańskie .
Pracował jako katecheta w : Toruniu , Szczecinku i Krakowie .
Zajmował się też duszpasterstwem akademickim .
