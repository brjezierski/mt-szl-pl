Tadżykistan
Tadżykistan , Republika Tadżykistanu ( , `` Dżumhurii Todżikiston '' ) – państwo w środkowej Azji ze stolicą w Duszanbe .
Tadżykistan graniczy z Uzbekistanem , Kirgistanem , Chinami i Afganistanem .
Geografia .
Tadżykistan nie ma dostępu do morza .
Niemal całą jego powierzchnię zajmują góry .
Ponad połowa terytorium leży powyżej 3000 m n.p.m .
Centralną część kraju zajmuje Hisaro-Ałaj z głównymi pasmami Gór Turkiestańskich , Gór Zarafszańskich , Gór Hisarskich i Gór Ałajskich .
Na południowym wschodzie rozciągają się góry Pamir .
Najwyżej położone punkty kraju to Szczyt Ismaila Samaniego ( 7495 m n.p.m . )
i Szczyt Lenina ( zwany w Tadżykistanie Szczytem Awicenny , 7134 m n.p.m . ) .
Północną część kraju stanowi urodzajna Kotlina Fergańska .
Granica z Afganistanem biegnie wzdłuż rzek Pandż i Amu-daria .
Przepływające przez kraj Amu-daria i Syr-daria zasilane są w dużej mierze wodą pochodzącą z topnienia śniegu w górach Kirgistanu i Tadżykistanu .
Klimat kontynentalny , wybitnie suchy .
Temperatury zróżnicowane w zależności od wysokości terenu .
W Tadżykistanie zdarzają się trzęsienia ziemi .
Historia .
W I wieku przed naszą erą terytorium dzisiejszego Tadżykistanu znajdowało się w obrębach imperiów Baktrii i Sogdiany ( to ostatnie obejmowało zachodnią część kraju ) .
W VII wieku ziemie te zostały podbite przez Arabów .
Jednak już w 819 roku rozpoczęła się władza perskiej dynastii Samanidów , za czasów których nastąpił rozwój kultury i sztuki .
W X wieku następuje wkraczanie plemion tadżyckich .
W XIII wieku ziemie Tadżykistanu znalazły się pod panowaniem Mongołów , a w XVI wieku stały się częścią Chanatu Buchary .
W 1868 roku północna część Tadżykistanu została przyłączona bezpośrednio do Carskiej Rosji .
Wówczas dzisiejszy Tadżykistan był wschodnią , najbardziej zacofaną częścią Emiratu Bucharskiego .
Związek Radziecki zdołał podporządkować sobie Tadżykistan dopiero w 1921 roku , po toczącym się od 1917 roku tadżyckim powstaniu przeciw władzy rosyjskiej .
Komuniści zniszczyli wiele miejscowości i zburzone zostały liczne meczety .
Od 1924 roku istniała Tadżycka ASRR w składzie Uzbeckiej SRR .
Pięć lat później Tadżykistan jako Tadżycka SRR stał się odrębną republiką radziecką .
Poza granicami republiki pozostały jednak tereny w większości zamieszkane przez Tadżyków wraz z miastami Buchara i Samarkanda .
Władze radzieckie nie przeprowadziły w Tadżykistanie żadnych znaczących inwestycji i przez cały okres istnienia ZSRR Tadżykistan był gospodarczo dyskryminowany , a poziom życia ludności utrzymywał się na bardzo niskim poziomie .
9 września 1991 roku ogłoszona została deklaracja niepodległości .
Tuż po uzyskaniu niepodległości masowe demonstracje zmusiły pierwszego prezydenta kraju Rahmona Nabijewa do ustąpienia .
Jeszcze przed tym rozpoczęła się niosąca szerokie zniszczenia wojna domowa , w której nowy prezydent Emomali Rahmonow mający poparcie wojsk Wspólnoty Niepodległych Państw walczył z pragnącymi przejąć władzę w młodym kraju organizacjami islamskimi wspieranymi przez zagranicznych dżihadystów , w tym ; Al-Kaidę , Islamską Republikę Afganistanu , talibów , a także uzbeckich islamistów .
Opozycja na czele z Islamską Partią Tadżykistanu domagała się utworzenia w Tadżykistanie państwa islamskiego , a następnie jego przemianę w kalifat rozciągnięty na całą Azję Środkową .
Wojna zakończyła się w 1997 roku zwycięstwem rządu , a na mocy układu Zjednoczona Opozycja Islamska uzyskała m.in .
30 % stanowisk w administracji rządowej .
Na podstawie Rezolucji Rady Bezpieczeństwa ONZ nr 738 z 29 stycznia 1992 roku Tadżykiestan został członkiem ONZ .
Od czasu deklaracji niepodległości Tadżykistan nie posiadał większego znaczenia na arenie międzynarodowej .
Zmieniło się to w momencie rozpoczęcia przez USA działań zbrojnych w Afganistanie .
Graniczący z tym państwem Tadżykistan stał się bazą dla wojsk amerykańskich .
Władze tadżyckie wykorzystały szansę , jaką daje strategiczne położenie kraju i wynegocjowały znaczną pomoc finansową ze strony Stanów Zjednoczonych .
