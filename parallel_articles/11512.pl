Tagajō
– miasto w Japonii , w prefekturze Miyagi , na wyspie Honsiu .
Ma powierzchnię 19,69 km2 .
W 2020 r. mieszkało w nim 63 276 osób , w 26 482 gospodarstwach domowych ( w 2010 r. 62 979 osób , w 24 047 gospodarstwach domowych ) .
Położenie .
Miasto leży około 350 km na północ od Tokio , we wschodniej części prefektury nad Oceanem Spokojnym .
