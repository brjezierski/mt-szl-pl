Taganrog
Taganrog ( ) – portowe miasto nad Morzem Azowskim w Rosji ( obwód rostowski ) .
Ludność ok. 248 tys .
( 2020 ) .
Jako twierdza założony przez Piotra Wielkiego w 1698 z przeznaczeniem na pierwszą bazę rosyjskiej marynarki wojennej .
Zburzony w 1712 .
Prawa miejskie uzyskał w 1775 .
Od początku XIX wieku ważny ośrodek handlowy , zaś od przełomu XIX i XX wieku również – przemysłowy .
Miasto położone na wzgórzu .
Ważny ośrodek przemysłu ciężkiego oraz ośrodek akademicki .
Zachował wiele ze swej historycznej architektury , co wpływa na jego atrakcyjność jako miasta turystycznego .
Miejsce urodzin Antona Czechowa – rosyjskiego nowelisty i dramatopisarza , Witolda Rowickiego – polskiego dyrygenta , pedagoga , organizatora życia muzycznego , oraz Sokrata Starynkiewicza – prezydenta Warszawy .
Historia .
Badania archeologiczne prowadzone w latach 30 .
XX wieku stwierdziły , że w miejscu obecnego Taganrogu w VI wieku p.n.e .
