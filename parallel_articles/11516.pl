Tainan
Tainan ( ) – miasto w południowo-zachodnim Tajwanie , nad Cieśniną Tajwańską .
Ma status miasta wydzielonego .
W 2010 roku liczyło 1 873 794 mieszkańców .
Ośrodek przemysłu cukrowniczego , spirytusowego , jutowego , papierniczego i chemicznego .
Miasto wydzielone Tainan zostało utworzone 25 grudnia 2010 roku , w wyniku połączenia miasta Tainan z powiatem o tej samej nazwie .
W mieście znajduje się port lotniczy Tainan .
