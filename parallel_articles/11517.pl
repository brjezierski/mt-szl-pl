Taishan ( Nowe Tajpej )
Taishan ( ) – dzielnica ( ) miasta wydzielonego Nowe Tajpej na Tajwanie .
Znajduje się w zachodniej części miasta .
23 czerwca 2009 komitet przy Ministrze Spraw Wewnętrznych Republiki Chińskiej zarekomendował przekształcenie dotychczasowego powiatu Tajpej ( ) w miasto wydzielone ; wszystkie gminy wiejskie ( ) , jak Taishan , gminy miejskie i miasta wchodzące w skład powiatu zostały przekształcone w dzielnice ( ) .
Ustawa weszła w życie 25 grudnia 2010 roku .
Populacja dzielnicy Taishan w 2016 roku liczyła 78 817 mieszkańców – 39 639 kobiet i 39 178 mężczyzn .
Liczba gospodarstw domowych wynosiła 28 230 , co oznacza , że w jednym gospodarstwie zamieszkiwało średnio 2,79 osób .
