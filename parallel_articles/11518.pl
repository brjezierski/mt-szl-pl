Taizhong
Taizhong ( , oficjalnie używana nazwa w alfabecie łacińskim : `` Taichung '' ) – miasto w zachodnim Tajwanie .
Ma status miasta wydzielonego .
W 2010 roku liczyło 2 665 134 mieszkańców .
Ośrodek szkolnictwa wyższego oraz spożywczego , włókienniczego , maszynowego i wysokich technologii .
Ważny węzeł komunikacyjny .
Miasto wydzielone Taizhong zostało utworzone 25 grudnia 2010 roku , w wyniku połączenia miasta Taizhong z powiatem o tej samej nazwie .
