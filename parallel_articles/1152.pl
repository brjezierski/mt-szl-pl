Dolar amerykański
Dolar amerykański ( oficjalna nazwa – `` United States dollar '' , międzynarodowy skrót – USD ) – oficjalna waluta Stanów Zjednoczonych , Portoryko , Mikronezji , Marianów Północnych , Palau , Wysp Marshalla , Panamy , Ekwadoru ( od 2000 ) , Salwadoru ( od 2001 ) , Timoru Wschodniego , Zimbabwe ( od 2009 ) , Turks i Caicos , Bonaire , Saby i Sint Eustatius od 2011 roku .
Jeden dolar amerykański dzieli się na sto centów amerykańskich .
Zapisuje się go za pomocą symbolu $ .
Talarem/dolarem określano początkowo powszechnie jedną uncję srebra w postaci monety bitej przez czeskiego hrabiego Schlicka w XVI wieku .
Schlick mieszkał w Dolinie Joachima , po niemiecku nazywanej Joachimsthal .
Monety hrabiego zyskały wielkie uznanie dzięki precyzji wykonania i czystości i były powszechnie nazywane „ Joachimsthalers ” , a później „ talarami ” .
Nazwa „ dolara ” pochodzi od „ talara ” .
Od 1861 roku można używać każdego kiedykolwiek wyemitowanego znaku pieniężnego w USA , jednak banknoty ze starszą datą emisji są rzadko spotykane .
Wartość dolara oparta była na parytecie złota .
Parytet przyjęty w 1900 roku wynosił 20,67 dolara , a przyjęty w 1934 roku 35 dolarów za uncję .
Od 1933 roku zniesiono wymienialność dolara na złoto dla własnych obywateli .
