Tajlandia
Tajlandia , Królestwo Tajlandii ( Prathet Thai , potocznie Muang Thai ) – państwo w południowo-wschodniej Azji , graniczące z Laosem i Kambodżą na wschodzie , z Malezją na południu oraz z Mjanmą ( Birmą ) na zachodzie i północnym zachodzie .
Dawniej państwo nosiło nazwę Syjam jako oficjalną do 11 maja 1949 .
Określenie „ Thai ” ( ไทย ) znaczy po tajsku „ wolny ” .
Geografia .
Tajlandia jest krajem nizinnym położonym na Półwyspie Indochińskim .
W środkowej części znajduje się Nizina Menamu ( pow .
