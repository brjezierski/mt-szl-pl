km² ) , na północnym wschodzie płytowa równina Korat ograniczona od południa pasmem Dongrak , a od Niziny Menamu górami Thiu Khao Phetchabun .
Północną i zachodnią część kraju zajmują młode ( mezozoiczne ) pasma górskie o przebiegu południkowym : Tanen Taunggyi ( Doi Inthanon , 2595 m n.p.m. , najwyższy szczyt Tajlandii ) i Doi Luang .
Długość linii brzegowej wynosi ponad 2600 km .
Wybrzeża są nizinne i silnie rozczłonkowane .
