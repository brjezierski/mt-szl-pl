Język tajski
Język tajski , syjamski – język z rodziny języków dajskich .
Jest językiem urzędowym Tajlandii .
Jako pierwszy język używany jest jedynie w centralnej części Tajlandii ( wokół Bangkoku ) .
Na północy , południu oraz w regionie północno-wschodnim mówi się odrębnymi językami tajskimi .
System fonetyczny .
