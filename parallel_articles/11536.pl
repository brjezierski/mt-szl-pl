Talloires
Talloires – miejscowość i dawna gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Górna Sabaudia .
W 2013 roku jej populacja wynosiła 1793 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Montmin oraz Talloires – utworzono nową gminę Talloires-Montmin .
Siedzibą gminy została miejscowość Talloires .
