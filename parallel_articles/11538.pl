Tambow
Tambow ( ros .
Тамбов ) – miasto obwodowe w środkowej części Rosji , na Nizinie Ocko-Dońskiej , nad Cną ( dorzecze Wołgi ) , w latach 1796–1928 stolica guberni tambowskiej .
W latach 1944–1960 w Tambowie mieściła się Tambowska Suworowska Szkoła Wojskowa .
W mieście znajduje się Teatr Dramatyczny .
W Tambowie urodzili się Faustyna Morzycka , polska działaczka niepodległościowa , i Gustaw Plewako , oficer marynarki wojennej RP w czasie II wojny światowej .
Historia .
Tambow został założony w 1636 roku .
