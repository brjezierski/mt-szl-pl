Tamilskie Tygrysy
Tamilskie Tygrysy ( Tygrysy Wyzwolenia Tamilskiego Ilamu , LTTE , , syng .
දෙමළ ඊළාම් විමුක්ති කොටි ) − organizacja tamilska żądająca utworzenia w północnej części Sri Lanki niepodległego państwa tamilskiego Ilamu .
Powstała w 1976 roku i prowadziła działalność do końca wojny domowej na Sri Lance w 2009 roku .
Jej przywódcą był Velupillai Prabhakaran .
Po uzyskaniu niepodległości przez Cejlon w 1948 syngaleska większość , dominująca w rządzie , stopniowo ograniczała prawa i dyskryminowała mniejszość tamilską .
Wskutek napięć między obiema grupami etnicznymi dochodziło wielokrotnie do krwawych pogromów Tamilów inspirowanych przez władze .
Do największych masakr doszło w 1956 , 1958 , 1961 , 1974 , 1977 i w 1983 .
Ostatnia data to wydarzenia zwane „ Czarnym Lipcem ” podczas których w stolicy Sri Lanki – Kolombo syngaleskie tłumy kierowane przez urzędników , polityków , żołnierzy , policjantów i mnichów buddyjskich zamordowały 1–3 tys .
Tamilów .
Organizacja Tamilskich Tygrysów podjęła próbę realizacji postulatów niepodległościowych w drodze przemocy i mimo znacznej przewagi wojsk rządowych przejęła kontrolę nad częścią terytorium objętego roszczeniami .
W 1992 i 1993 walki między partyzantami tamilskimi a armią rządową nasiliły się .
