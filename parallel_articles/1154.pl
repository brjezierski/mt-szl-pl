W historii .
W starożytnym teatrze greckim była to widownia na wolnym powietrzu , rozłożona półkoliście na zboczach pagórków , wyposażona w kamienne siedzenia .
W starożytnym Rzymie budowla ta wykształciła się z wydzielonego na forum dziedzińca , na którym walczyli gladiatorzy .
Wokół dziedzińców zaczęto umieszczać ławy , które z czasem stawiano w kilku rządach na usypanym wale .
Później całość zaczęto otaczać murem .
Ostatecznie amfiteatr stał się wielkim kompleksem rozrywkowym z widownią ( `` cavea '' ) mieszczącą często dziesiątki tysięcy widzów , z wieloma pomieszczeniami pomocniczymi , masztami do rozpinania nad nim osłony ( `` velarium '' ) i wieloma innymi udogodnieniami .
W czasach imperium rzymskiego zbudowano setki amfiteatrów , głównie w Italii i na zachodzie .
Zdecydowanie mniejsza liczba amfiteatrów we wschodnich prowincjach imperium była efektem niechęci ich mieszkańców do krwawych rozrywek , w których gustowali Rzymianie .
Najsłynniejszym amfiteatrem jest rzymskie Koloseum .
Współcześnie nazwą amfiteatr określa się budowlę widowiskową z kolistą lub półkolistą , wznoszącą się do góry widownią .
