Język tamilski
Język tamilski ( tamil ) , tamilski : ( tamizh ) – język z rodziny drawidyjskiej , jej grupy południowej .
Drugi , obok języka telugu , pod względem liczby użytkowników język tej rodziny – posługuje się nim około 65 mln Tamilów .
Zamieszkują oni głównie południowoindyjski stan Tamil Nadu ( 60 mln ) oraz wyspę Cejlon ( 4 mln ) .
Językiem tym posługuje się także ponad milion emigrantów tamilskich w Malezji i Singapurze .
Język urzędowy Singapuru , Sri Lanki i stanu Tamil Nadu .
