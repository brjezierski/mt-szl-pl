Tampa
Tampa – miasto w Stanach Zjednoczonych , w stanie Floryda , ośrodek administracyjny hrabstwa Hillsborough , położone na zachodnim wybrzeżu półwyspu Floryda , nad zatoką Tampa , nad ujściem rzeki Hillsborough .
Jest to trzecie pod względem wielkości miasto na Florydzie , w 2020 roku liczyło 384 959 mieszkańców .
Obszar metropolitalny Tampa–St .
Petersburg–Clearwater , zamieszkany przez 3 175 275 osób ( 2020 ) , jest drugą co do wielkości aglomeracją Florydy .
Według `` Tampa Bay Times '' , CompTIA uznała Tampę za jedno z 20 najważniejszych „ miast technologicznych ” w Stanach Zjednoczonych .
Swoje siedziby mają tutaj takie firmy jak Tech Data , Jabil Circuit , WellCare Health Plans , SYKES , Raymond James , The Mosaic Company czy HSN .
Port w Tampie jest największym portem głębokowodnym na terenie Florydy .
W mieście znajduje się stacja kolejowa Tampa Union Station .
