Tampere
Tampere szw .
`` Tammerfors '' – miasto w południowo-zachodniej Finlandii leżące na przesmyku między jeziorami Näsijärvi i Pyhäjärvi w regionie Pirkanmaa .
Jeden z największych ośrodków przemysłowych w kraju , przez co bywa nazywane `` Manchesterem Finlandii '' , port śródlądowy i węzeł kolejowy .
Ludność : ponad 220 tys .
mieszkańców , aglomeracja – 340 tys .
W skład zespołu miejskiego wchodzą : Nokia , Pirkkala , Kangasala i Aito .
W mieście znajduje się część koncernu Metso Automation ( przemysł papierniczy , mineralny i automatyka przemysłowa ) .
Uniwersytet założony w 1925 r. , uniwersytet techniczny w 1972 r. W mieście działał także polski konsulat honorowy ( obecnie nieaktywny ) .
Geografia .
Tampere leży na przesmyku między jeziorami Näsijärvi i Pyhäjärvi w południowo-zachodniej Finlandii na północny zachód od Helsinek .
Różnica poziomów jezior wynosi 18 metrów , a zbiorniki połączone są kaskadą Tammerkoski , na której się znajduje siłownia .
