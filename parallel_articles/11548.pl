Portugalczycy i Brytyjczycy .
Po wyparciu muzułmanów z południa Półwyspu Iberyjskiego , Tanger ponownie znalazł się na granicy świata islamskiego , a w 1471 roku został zdobyty przez Portugalczyków .
W 1661 roku został wraz z Bombajem oddany w ręce Brytyjczyków jako wiano dla księżniczki Katarzyny Bragançy , narzeczonej króla Karola II Stuarta .
Brytyjczycy szybko uznali jednak obronę miasta przed ciągłymi atakami Arabów za nieopłacalną i w 1684 opuścili Tanger pozostawiając miasto w rękach sułtana Mulaja Ismaila .
Niepodzielne panowanie arabskie w Tangerze trwało aż do początków XX wieku .
Strefa międzynarodowa .
