Blisko sto lat później Tanger znalazł się pod władzą Bizancjum .
W VII wieku władzę nad miastem na krótko objęli Wizygoci , jednak już w 707 roku tereny dzisiejszego Maroka , wraz z Tangerem , znalazły się pod panowaniem arabskim .
