Tangshan
Tangshan , Tangszan ( chiń .
唐山 ; pinyin : `` Tángshān '' ) – miasto o statusie prefektury miejskiej w Chinach , we wschodniej części prowincji Hebei , około 145 km na wschód od Pekinu .
W 2010 roku liczba mieszkańców miasta wynosiła 1 627 819 .
Prefektura miejska w 1999 roku liczyła 11 022 165 mieszkańców .
Miasto stanowi główny ośrodek przemysłowy Kajluańskiego Zagłębia Węglowego .
Rozwinięty przemysł wydobywczy ( węgiel kamienny ) , hutniczy , koksochemiczny , maszynowy , cementowy , ceramiczny i włókeinniczy .
Tangshan jest ważnym węzłem komunikacyjnym .
Tangshan powstał w latach 40 .
XX wieku z połączenia rozproszonych osad górniczych .
Miasto zostało poważnie zniszczone przez silne trzęsienie ziemi o sile 8,2 w skali Richtera , które nawiedziło je rankiem 28 lipca 1976 roku .
