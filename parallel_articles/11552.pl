Tanita Tikaram
Tanita Tikaram ( ur .
12 sierpnia 1969 w Münsterze , Niemcy ) – brytyjska piosenkarka i autorka piosenek popowo-folkowych .
Życiorys .
Jej matka Fatimah Rohani pochodzi z Malezji , ze stanu Sarawak na wyspie Borneo , ojciec Pramod Tikaram ma hindusko-fidżyjskie pochodzenie , jest brytyjskim żołnierzem .
Wychowywała się w Basingstoke w południowej Anglii .
Jest młodszą siostrą aktora Ramona Tikarama .
Obecnie mieszka w Londynie , w dzielnicy Primrose Hill .
