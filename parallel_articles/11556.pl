Tanzania
Tanzania , Zjednoczona Republika Tanzanii ( suahili `` Jamhuri ya Muungano wa Tanzania '' ) – państwo we wschodniej Afryce powstałe z połączenia dawnych kolonii brytyjskich : Tanganiki ( do I wojny światowej niemieckiej ) oraz brytyjskiego Zanzibaru , co symbolizowała nazwa utworzona z pierwszych sylab nazw obu krajów TAN+ZAN .
W skład Tanzanii jako republiki federacyjnej weszła również wyspa Pemba położona blisko Zanzibaru .
Stolicą Tanzanii jest Dodoma , do 1981 roku było nią Dar es Salaam ( obecnie stolica administracyjna i siedziba rządu ) .
Geografia .
Większą część powierzchni Tanzanii zajmuje Wyżyna Wschodnioafrykańska ( wys .
900–1500 m n.p.m . )
przedzielona wypełniony osadami wulkanicznymi Wielkim Rowem Wschodnim , wypełnionym jeziorami Natron , Eyasi i Manyara .
Wielki Rów Wschodni otoczony jest masywem Kilimandżaro .
W północnej części kraju ciągną się rozległe równiny , w tym Serengeti Plains , Step Masajów , Step Wembere , gdzieniegdzie występują ostańce skalne .
Na południu znajdują się w znacznym stopniu zerodowane pasma górskie ( Rubeho , Uluguru i Mbarika ) .
Wzdłuż zachodniej granicy rozciągają się pasma gór zrębowych – Kipengere Range , Poroto i Mgeya , opadające stromym uskokiem do Wielkiego Rowu Zachodniego i ulokowanych w nim jezior .
Na wybrzeżu występuje pas nizin , szeroki na 50–100 km , w obszarze delty Rufidżi na 200 km .
Należące do Tanzanii wyspy Pemba i Zanzibar otoczone są koralami .
Klimat równikowy monsunowy , na wyspach i wybrzeżu wilgotny , na płaskowyżu suchszy .
W północnej części kraju występują dwie pory deszczowe ( od marca do maja i od września do listopada ) , na pozostałym obszarze jedna – od listopada do kwietnia lub maja .
Często występują burze .
W pasie nadbrzeżnym sieć hydrograficzna jest gęsta , we wnętrzu kraju rzeki mają charakter głównie okresowy .
