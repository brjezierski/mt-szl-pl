Taphrina cerasi
Taphrina cerasi ( Fuckel ) Sadeb .
– gatunek grzybów z klasy szpetczaków ( `` Taphrinomycetes '' ) .
Grzyb mikroskopijny , pasożyt czereśni i wiśni , wywołujący u nich grzybową chorobę roślin o nazwie czarcie miotły czereśni .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Taphrina , Taphrinaceae , Taphrinales , Taphrinomycetidae , Taphrinomycetes , Taphrinomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1870 r. Leopold Fuckel nadając mu nazwę `` Exoascus cerasi '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Richard Sadebeck w 1890 r .
Synonimy :
W niektórych opracowaniach `` Taphrina cerasi '' jest uważana za synonim `` Taphrina wiesneri '' ( Ráthay ) Mix , jednak według Index Fungorum są to odrębne gatunki .
Charakterystyka .
Znane jest występowanie `` Taphrina cerasi '' w Ameryce Północnej , Europie i Japonii .
Jest monofagiem występującym tylko na czereśniach i wiśniach .
Teleomorfa jest pasożytem obligatoryjnym .
Tworzy dikariotyczną grzybnię pod kutykulą porażonych roślin , a na jej powierzchni zwartą , palisadową warstwę worków , która powstaje bezpośrednio z komórek workotwórczych .
Worki są cienkościenne , zwykle 8-zarodnikowe , o trwałych ścianach .
Pomiędzy workami brak wstawek .
Askospory szkliste , niemal kuliste .
Anamorfa jest saprotrofem w postaci fazy drożdżoidalnej .
Tworzą ją zarodniki powstałe z pączkujących askospor .
Charakterystycznym objawem porażenia czereśni i wiśni przez `` Taphrina cerasi '' są czarcie miotły w ich koronach .
