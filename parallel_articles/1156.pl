( 12100 ) Amiens
( 12100 ) Amiens – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4,19 lat w średniej odległości 2,60 j.a .
Została odkryta 25 kwietnia 1998 roku przez Erica Waltera Elsta w Europejskim Obserwatorium Południowym .
Jej nazwa pochodzi od miasta Amiens – stolicy Pikardii .
