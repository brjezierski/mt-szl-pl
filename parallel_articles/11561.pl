Taraszkiewica
Taraszkiewica , klasyczny wariant ( biał .
тарашкевіца , `` taraškievica '' ; клясычны правапіс , `` klasyčny pravapis '' ) – potoczna nazwa jednego z wariantów ortografii języka białoruskiego , opierającego się na regułach stosowanych przed reformą językową z 1933 roku i będącego systemem alternatywnym w stosunku do ortografii oficjalnej .
W szerszym rozumieniu nazwa `` taraszkiewica '' dotyczy również pewnych różnic w gramatyce i leksyce ( w stosunku do oficjalnych norm językowych ) .
Taraszkiewica a inne warianty .
Nazwa podkreśla bliskość alternatywnego wariantu do języka stosowanego w twórczości Bronisława Taraszkiewicza w 1918 roku .
Spór odnośnie do „ taraszkiewicy ” nabrał rozmachu po ogłoszeniu niepodległości Białorusi w 1991 roku .
Od około 1994 roku jako zalecany synonim ( np .
w artykule Wincuka Wiaczorki w periodyku Spadczyna , 1994 ) używana jest nazwa „ klasyczny wariant ” .
