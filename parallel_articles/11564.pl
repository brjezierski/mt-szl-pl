Język tarifit
Język tarifit ( arab .
`` rifia '' ) – język berberyjski używany przez 1,7 mln osób w północnej Afryce , z czego 1,5 mln w Maroku .
Nazwa języka pochodzi od gór Rif .
Język tarifit zapisywany jest oficjalnie alfabetem tifinagh , w użyciu jest również pismo arabskie i alfabet łaciński .
