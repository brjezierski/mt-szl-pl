Według stanu z dnia 1 lipca 1952 roku gmina składała się z 24 gromad .
1 września 1952 roku do gminy Tarków przyłączono część obszaru gminy Krześlin , odłączono od niej natomiast inną część obszaru , którą przyłączono do gminy Przesmyki .
Gmina została zniesiona 29 września 1954 roku wraz z reformą wprowadzającą gromady w miejsce gmin .
Jednostki nie przywrócono 1 stycznia 1973 roku po reaktywowaniu gmin , utworzono natomiast jej terytorialny odpowiednik , gminę Paprotnia .
