Tarnobrzeg
Tarnobrzeg – miasto w Polsce , na prawach powiatu , położone w województwie podkarpackim .
Siedziba władz powiatu tarnobrzeskiego w latach 1867–1975 oraz od 1999 .
Siedziba władz województwa tarnobrzeskiego w latach 1975–1998 .
W latach 1973–1976 miasto było siedzibą gminy wiejskiej Tarnobrzeg .
Do początku lat 90 .
XX wieku największy w Polsce ośrodek wydobycia i przetwórstwa siarki .
Według danych GUS z 31 grudnia 2020 roku , Tarnobrzeg liczył 46 360 mieszkańców .
Tarnobrzeg uzyskał lokację miejską w 1593 roku , ale nie została ona zrealizowana , ponowne nadanie praw miejskich przed 1681 rokiem .
Nazwa miasta .
Nazwa Tarnobrzega nawiązuje do rodziny szlacheckiej , która to ufundowała miasto – Tarnowskich .
Nazwa Tarnobrzeg przeważyła nad innymi proponowanymi nazwami : „ Tarnodwór ” , „ Nowo Dwór ” czy „ Nowy Tarnów ” .
