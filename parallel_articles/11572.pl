Tarnowa Łąka
Tarnowa Łąka ( dawniej Tarnowałąka ) ( niem .
Tharlang ) – wieś w Polsce położona w województwie wielkopolskim , w powiecie leszczyńskim , w gminie Rydzyna .
W latach 1975–1998 miejscowość administracyjnie należała do województwa leszczyńskiego .
Historia .
Pierwsze zapisy o Tarnowej Łące pochodzą z 1410r .
W początku XV w. stała się własnością Jana z Czerniny i Rydzyny .
W rękach Rydzyńskich pozostawała do lat 80 .
XVII w. , istniały wówczas dwa folwarki , które przeszły w ręce Leszczyńskich .
