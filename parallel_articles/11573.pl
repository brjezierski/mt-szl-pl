Tarnowskie Góry
Tarnowskie Góry ( ; , , `` Tarnovské Hory '' ; ) – miasto w południowej Polsce , w województwie śląskim , siedziba powiatu tarnogórskiego , na północnym krańcu Górnośląskiego Okręgu Przemysłowego ( GOP ) , historycznie na Górnym Śląsku .
W latach 1975–1998 miasto administracyjnie należało do województwa katowickiego .
W 2017 tarnogórska Kopalnia rud ołowiu , srebra i cynku wraz z systemem gospodarowania wodami podziemnymi została wpisana na listę światowego dziedzictwa UNESCO .
Według danych GUS z 31 grudnia 2020 r. , Tarnowskie Góry liczyły 61 756 mieszkańców i były pod względem liczby ludności osiemnastym miastem w województwie śląskim , a także 67. spośród najludniejszych miast w Polsce .
Tarnowskie Góry są w Polsce największym spośród miast i zarazem jedynym spośród miast ludniejszych niż 50 000 , w których wybiera się burmistrza .
Położenie geograficzne .
Tarnowskie Góry leżą w środkowo-południowej części powiatu tarnogórskiego .
Przez miasto przepływa m.in .
Stoła ( przez dzielnice Lasowice , Sowice i Strzybnicę ) , Drama ( przez Repty Śląskie ) i struga Pniowiec ( przez dzielnicę Pniowiec ) .
Miasto leży na skraju Niziny i Wyżyny Śląskiej .
Tarnowskie Góry są głównym miastem Garbu Tarnogórskiego .
