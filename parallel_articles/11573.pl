Klimat .
Tarnowskie Góry leżą w obrębie śląsko-krakowskiej dzielnicy klimatycznej .
Charakteryzuje ją przewaga wpływów oceanicznych nad kontynentalnymi oraz sporadyczne oddziaływanie docierających tu od południowego zachodu przez Bramę Morawską mas powietrza zwrotnikowego .
Docierają tu również zimne masy powietrza arktycznego z północy – głównie w chłodnej połowie roku .
Średnia temperatura roczna wynosi 7,7 °C , zaś suma opadów atmosferycznych wynosi 699 mm w ciągu roku .
Okres wegetacyjny trwa w Tarnowskich Górach średnio 246 dni .
