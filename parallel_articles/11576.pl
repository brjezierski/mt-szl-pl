Tarnów
Tarnów ( ) – miasto w Polsce , na prawach powiatu , położone w województwie małopolskim .
Leży przy ujściu Białej do Dunajca .
Jest miastem centralnym aglomeracji tarnowskiej .
W mieście znajduje się siedziba powiatu tarnowskiego .
Miasto lokowane przez Spycimira herbu Leliwa , na podstawie dokumentu wystawionego przez kancelarię króla Władysława Łokietka z 7 marca 1330 roku .
Zachował się historyczny zespół architektoniczno-urbanistyczny miasta .
Tarnów zajmuje powierzchnię 72,38 km² , którą zamieszkuje ponad 100 tysięcy mieszkańców .
Miasto jest ważnym ośrodkiem turystycznym , kulturalnym i gospodarczym w Małopolsce , a także węzłem komunikacyjnym przy autostradzie A4 oraz linii kolejowej E 30 .
Na terenie osiedla Mościce znajduje się siedziba Grupy Azoty , największej grupy kapitałowej branży chemicznej w kraju oraz Międzynarodowe Centrum Bezpieczeństwa Chemicznego .
W mieście działa konsulat honorowy Ukrainy .
Tarnów jest członkiem Związku Miast Polskich .
