Tasmania
Tasmania – wyspa u południowo-wschodnich wybrzeży kontynentu australijskiego , a zarazem najmniejszy stan Australii .
W skład stanu Tasmania wchodzą też mniejsze wyspy w Cieśninie Bassa oraz wyspa Macquarie – pow .
128 km² ( w południowej części Oceanu Spokojnego ) .
Geografia .
Wyspa Tasmania , położona ok. 300 km na południe od wybrzeża Australii , jest górzysta i ma wspólną historię geologiczną z górami we wschodniej części Australii ( Wielkie Góry Wododziałowe ) , od których oddziela ją Cieśnina Bassa i jęzor półotwartego Morza Tasmana .
Znaczną część wyspy zajmuje Płaskowyż Centralny , zbudowany z osadów paleozoicznych przykrytych jurajskimi skałami magmowymi , ograniczony od północy i wschodu przeszło 600-metrowym uskokiem .
W najwyższych partiach górskich występują ślady zlodowacenia .
Na północnym wschodzie wznosi się masyw Ben Lomond ( do 1573 m n.p.m .
) , wyraźnie oddzielony od płaskowyżu obniżeniem o nazwie Tamar .
Najwyższym szczytem wyspy jest Mount Ossa ( 1617 m n.p.m . )
położony w północnej części .
Tasmania ma urozmaiconą linię brzegową , gęstą sieć rzek i liczne jeziora polodowcowe .
Brzegi wyspy są niewysokie i wyrównane , z wyjątkiem południowego wybrzeża , które tworzą wysokie klify .
Flora .
Tasmania jest najbardziej zalesionym stanem Australii .
Znaczną część wyspy porastają lasy z przewagą eukaliptusa ( na północy i wschodzie ) lub buka południowego ( na południowym zachodzie ) .
