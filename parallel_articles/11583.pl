Diabeł tasmański
Diabeł tasmański ( `` Sarcophilus harrisii '' ) – gatunek mięsożernego ssaka , torbacza z rodziny niełazowatych ( Dasyuridae ) .
Występuje na wolności jedynie na australijskiej wyspie Tasmanii .
Osiąga wielkość niewielkiego psa i jest największym współczesnym mięsożernym torbaczem , po wyginięciu wilkowora tasmańskiego ( wilka workowatego ) w 1936 .
Ma krępe , umięśnione ciało , czarne futro , ostry zapach , bardzo głośny i niepokojący pisk , doskonały węch i charakterystyczny sposób pożywiania się .
Poluje na zdobycz i poszukuje padliny , zjada też produkty spożywcze , jeśli bytuje w pobliżu osiedli ludzkich .
Zazwyczaj prowadzi samotny tryb życia , czasami żywi się razem z innymi diabłami i wypróżnia się we wspólnym miejscu .
W odróżnieniu od większości niełazowatych , ma efektywne mechanizmy termoregulacji organizmu , co pozwala mu zachować aktywność w południe , nie przegrzewając się .
Pomimo pulchnego wyglądu zaskakuje szybkością i wytrzymałością .
Potrafi wspinać się na drzewa i przepływać rzeki .
Uważa się , że torbacze dostały się do Australii z Ameryki Południowej , gdy dziesiątki milionów lat temu lądy te wchodziły w skład superkontynentu Gondwany .
Ewoluowały , gdy Australia stawała się bardziej sucha .
Znaleziono skamieliny gatunków podobnych do diabłów tasmańskich , nie wiadomo jednak , czy są to przodkowie współczesnych diabłów .
Nie wiadomo również , kiedy zaczęły się regionalne wyginięcia diabłów na terenie Australii kontynentalnej .
Większość danych świadczy o trzech populacjach reliktowych około 3000 lat temu .
Zęby znalezione w Auguście w Australii Zachodniej datuje się na 430 lat temu , ale archeolog Oliver Brown datuje wymarcie diabłów na kontynencie około 3000 lat temu .
Za ich wymarcie zazwyczaj wini się dingo , które nie występują na Tasmanii .
Ponieważ uważano , że diabły zagrażają zwierzynie hodowlanej i zwierzętom , z których pozyskiwano futra , polowano na nie , doprowadzając do zagrożenia wyginięciem .
W 1941 gatunek został objęty oficjalną ochroną .
Od tego czasu naukowcy utrzymują , że wcześniejszy pogląd , jakoby diabły tasmańskie były największym zagrożeniem dla zwierząt hodowlanych , był przesadzony i nieuzasadniony .
Diabły nie są monogamiczne .
Rozród jest dlań energochłonny , obfituje we współzawodnictwo .
Samce walczą o samice .
Strzegą swych partnerek , by zapobiec niewierności .
Owulacja u samicy może wystąpić trzykrotnie podczas sezonu rozrodczego .
80 % dwuletnich samic zachodzi w ciążę podczas rocznego okresu rozrodu .
Samica przeżywa średnio 4 okresy godowe .
Ciąża trwa zaledwie 3 tygodnie .
Noworodki są różowe , nie mają futra ani rozwiniętych cech pyska .
Ważą około 0,20 g. W torbie znajdują się tylko 4 sutki .
