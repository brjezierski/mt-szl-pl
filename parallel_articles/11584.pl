Język tashelhiyt
Język tashelhiyt ( tashelhit , tachelhit , shilha , szilha , tasoussit ) – najczęściej używany język północnoberberyjski , spotykany głównie w dolinie Sus w południowo-zachodnim Maroku , a w mniejszym stopniu w Algierii ( przy granicy z Marokiem ) i wśród emigrantów we Francji .
Ma wiele zapożyczeń z arabskiego .
W Algierii istnieje dialekt tego języka zwany `` susiua '' .
Literatura .
Istnieją liczne tradycje ustnej literatury w tym języku .
We Francji i w Holandii zgromadzono zbiory religijnych rękopisów w tashelhit .
Pismo .
Najczęściej używany jest system pisania – tifinagh .
We wcześniejszych okresach dominowało pismo arabskie .
Powstawały ( i powstają ) też zapisy łacinką .
