Nazwa .
.
Według ustaleń Rady Języka Polskiego za poprawniejszą uznawana jest forma Taszkent , umieszczana we współczesnych słownikach na pierwszym miejscu jako nazwa hasłowa .
Komisja Standaryzacji Nazw Geograficznych poza Granicami Rzeczypospolitej Polskiej podaje ją jako obowiązujący polski egzonim .
Transport .
W mieście znajduje się stacja kolejowa i metro .
Taszkent posiada międzynarodowy port lotniczy .
Linia tramwajowa funkcjonująca w mieście od 1912 roku została zlikwidowana w 2016 roku dezycją władz .
Sport .
Od 1999 roku rozgrywany jest kobiecy turniej tenisowy , Tashkent Open , zaliczany do rozgrywek cyklu WTA Tour .
