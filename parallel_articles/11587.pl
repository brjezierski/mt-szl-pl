Tatabánya
Tatabánya – miasto na prawach powiatu , położone w komitacie Komárom-Esztergom .
Położenie .
Miasto położone jest w południowej części komitatu , w dolinie między wzgórzami Gerecse i Vértes , 55 km na zachód od Budapesztu .
Z powodu swojego położenia Tatabánya stała się ważnym krajowym węzłem komunikacyjnym .
Na jej obrzeżach przebiega autostrada M1 Budapeszt-Wiedeń , przez nią przebiega także linia kolejowa łącząca te dwie stolice .
