Tatarstan ( linie lotnicze )
Tatarstan – zlikwidowane rosyjskie linie lotnicze z siedzibą w Kazaniu .
Zostały założone w 1993 i były narodowymi liniami lotniczymi Tatarstanu .
Głównym węzłem był port lotniczy Kazań .
W swojej flocie posiadały samoloty Jak-42 , Tu-154 , Boeing 737 oraz Airbus A319 .
