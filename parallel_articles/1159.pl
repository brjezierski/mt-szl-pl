Among Us
Among Us – strategiczna komputerowa gra wieloosobowa opracowana i wydana przez InnerSloth .
Gra ukazała się 15 czerwca 2018 na platformę Android , 17 czerwca tego samego roku na iOS-a oraz 16 listopada na Microsoft Windows .
15 grudnia 2020 studio wydało wersję na konsolę Nintendo Switch .
Wersje na konsole PlayStation 4 , PlayStation 5 , Xbox One oraz Xbox Series X/S wydano 14 grudnia 2021 .
W sierpniu 2020 zapowiedziano `` Among Us 2 '' .
Studio jednak anulowało kontynuację , by skupić się na pierwszej części .
Rozgrywka .
Gracze dzielą się na członków załogi ( ) oraz od jednego do trzech oszustów ( ) .
Rolą członków załogi jest wykonywanie zadań na statku , a oszustów – sabotowanie misji i eliminowanie członków załogi .
Gracze na początku rozgrywki otrzymują informację , ilu antagonistów jest na statku .
Są oni eliminowani poprzez demokratyczne głosowania , podczas których członkowie załogi konsultują się i na podstawie swoich obserwacji wyłaniają podejrzanych .
Osoby , które uzyskały najwięcej głosów , zostają wyrzucone ze statku i giną .
Gdy wszyscy oszuści zostaną zlikwidowani lub gdy członkowie wykonają wszystkie zadania , wygrywa załoga .
Gdy oszuści wyeliminują dużą część załogi lub zniszczą ważne części statku , wygrywają .
W grze początkowo udostępniono trzy mapy – The Skeld , Mira HQ i Polus .
W 2020 roku zapowiedziano wydanie czwartej mapy – The Airship .
Ukazała się ona 31 marca 2021 roku .
Historia .
Inspiracją do stworzenia `` Among Us '' była towarzyska gra `` Mafia '' .
Produkcja została oparta na silniku Unity .
W czasie produkcji gra nazywała się „ spacemafia ” i pod takim tytułem została początkowo wydana .
Początkowo gra umożliwiała jedynie rozgrywkę w sieci lokalnej i była przeznaczona na urządzenia z systemem Android i iOS .
Twórcy uwzględnili jednak prośby graczy i dodali tryb online , a sama gra trafiła na platformę Steam .
W 2020 roku streamerzy przyczynili się do wzrostu popularności gry .
Według danych z września 2020 liczba pobrań gry wyniosła 100 mln .
10 grudnia 2021 zapowiedziano wydanie wersji gry wykorzystującej gogle VR .
Odbiór .
Gra spotkała się z pozytywnym odbiorem recenzentów , uzyskując w wersji na platformę Windows według agregatora Metacritic średnią z 9 ocen wynoszącą 85/100 oraz w wersji na Nintendo Switch 79/100 z 9 recenzji .
W październiku 2020 `` Among Us '' była najczęściej pobieraną grą z App Store .
24 listopada 2020 podczas gali Golden Joystick Awards otrzymała nagrodę dla „ najbardziej przełomowej gry ” .
10 grudnia tego samego roku podczas The Game Awards została wyróżniona w dwóch kategoriach : dla „ najlepszej gry mobilnej ” i „ najlepszej gry wieloosobowej ” .
Wygrała też 34. edycję Nickelodeon Kids ’ Choice Awards , zdobywając tytuł najlepszej gry .
`` Among Us '' stał się inspiracją wielu memów internetowych , które rozpowszechniane są w mediach społecznościowych .
