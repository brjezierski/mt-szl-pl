Tatra KT4
Tatra KT4 – typ tramwaju wytwarzanego w latach 1974–1997 w zakładach ČKD w Pradze w Czechosłowacji ( później Czechy ) .
Konstrukcja .
KT4 to dwuczłonowy , jednokierunkowy tramwaj , wyposażony w czworo harmonijkowych drzwi .
Pudło osadzone jest na dwóch dwuosiowych wózkach .
Wóz nie posiada niskiej podłogi .
Pojazd należy do rodziny tramwajów tzw .
„ krótkoprzegubowych ” – pudło każdego członu podparte jest na wózku mającym możliwość ruchu obrotowego względem członu , jednocześnie przegub pozwala na ruch członów względem siebie .
Stabilną jazdę zapewnia układ dźwigni zabudowanych pod wagonami .
Produkowane odmiany .
KT4D .
Najliczniej produkowaną wersją jest KT4D przeznaczona dla miast NRD , produkowana w latach 1974 – 1990 .
Wagony tego typu znalazły się w wielu miastach Niemiec Wschodnich – Berlinie , Poczdamie , Chociebużu , Erfurcie , Frankfurcie nad Odrą , Gerze , Görlitz , Gotha , Brandenburgu , Lipsku , Plauen i Zwickau .
W roku 1982 wyprodukowano 2 prototypowe wagony KT4Dt o rozruchu tyrystorowym .
W 1983 ruszyła ich produkcja seryjna i do 1987 wszystkie 99 wyprodukowanych egzemplarzy trafiło do Berlina Wschodniego .
Ogółem wyprodukowano 1042 wagony w różnych wersjach przeznaczonych dla miast NRD .
W latach 90. większość z nich poddała swoje wagony modernizacjom polegającym głównie na montażu nowych kabin i pulpitu motorniczego , drzwi odskokowych , połówkowych pantografów , nowego układu elektrycznego i zmianom w przedziale pasażerskim .
W miastach Chociebuż i Brandenburg ( KTNF6 ) oraz Gera ( KTNF8 ) w modernizowanych wagonach wprowadzono część niskopodłogową .
Tatry KT4 są w Niemczech zastępowane nowocześniejszym taborem , a wycofywane wozy znajdują odbiorców w innych krajach europejskich .
Tym sposobem rozpoczęto eksploatację KT4D w Rumunii ( Oradea ) , na Ukrainie ( Lwów ) i na Węgrzech ( Szeged ) .
W Polsce ( Szczecin ) eksploatowanych jest 73 wagonów KT4DtM , zakupionych od berlińskiego przewoźnika BVG .
Wagony te przeszły gruntowną modernizację w zakładach Siemens , dzięki czemu znacznie różnią się od fabrycznych KT4Dt .
W latach 2011-2015 , 34 wagony zostały ponownie zmodernizowane przez Tramwaje Szczecińskie .
Tramwaje KT4D są eksploatowane w innych państwach europejskich nie tylko liniowo – wagon zakupiony w Gerze używany jest w czeskim Libercu jako wóz roboczy .
Wagony Tatra KT4D w Berlinie .
Historia wagonów KT4D w stolicy Niemiec , Berlinie rozpoczęła się 28 marca 1976 roku , kiedy pierwsza Tatra KT4D przybyła do Berlina Wschodniego , stolicy ówczesnej NRD .
Trzy dostarczone tramwaje rozpoczęły służbę 11 września tego roku na linii 76 .
Jednak dopiero rok później , bo w grudniu 1977 roku dostarczono tramwaje KT4D pierwszej serii różniące się od zerowej serii malowaniem .
Seria z lat 1981 – 1982 różniła się siedzeniami .
W 1994 roku rozpoczęto modernizację berlińskich wagonów KT4D , którą wykonała firma Siemens AG .
Zmodernizowane wagony zostały oddane do użytku w 1995 roku i oznaczone jako typ KT4DtM .
W roku 2007 berlińskie przedsiębiorstwo komunikacyjne sprzedało 21 wagonów Tatra KT4DtM dla Szczecina , a w 2010 kolejnych 11 wagonów .
W roku 2012 spółka Tramwaje Szczecińskie zakupiła kolejne 42 Tatry KT4Dt , które dostarczono w latach 2012-2013 .
