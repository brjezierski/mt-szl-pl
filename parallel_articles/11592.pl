Tatraea dumbirensis
Tatraea dumbirensis ( Velen . )
Svrček – gatunek grzybów z rzędu tocznikowców ( `` Helotiales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tatraea , Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1934 Josef Velenovský , nadając mu nazwę `` Helotium dumbirense '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu w roku 1993 Mirko Svrček .
Synonimy naukowe :
Owocniki typu apotecjum na trzonku lub siedzące .
