Morfologia .
Pojedynczy ma średnicę około 5–15 mm , owocniki najczęściej występują w małych lub dużych grupach , tylko sporadycznie występujące jako samotne owocniki .
Kształt miseczkowaty lub płaski , górna powierzchnia gładka , blado-szara do szaro-białej .
Dolna powierzchnia jest delikatnie szorstka , podobna kolorem do górnej .
Trzon do 15 mm długości i 1,5 mm szerokości , niekiedy znacznie krótszy , gładki lub delikatnie owłosiony , kolorystycznie zbliżony do górnej powierzchni .
