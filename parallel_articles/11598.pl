Taunton ( stacja kolejowa )
Taunton – węzłowa stacja kolejowa w mieście Taunton w hrabstwie Somerset na linii Bristol - Exeter z bezpośrednimi połączeniami ze stacjami London Paddington , Bristol Temple Meads i Penzance .
Podobnie jak większość stacji kolejowych na odcinku Bristol - Exeter , zaprojektowana przez Isambarda Kingdom Brunela .
Początkowo jednoperonowa , stacja uzyskuje kształt zbliżony współczesnemu pod koniec XIX w. , po wydłużeniu peronów .
Południowy budynek jest zabytkiem grupy II .
Ruch pasażerski .
