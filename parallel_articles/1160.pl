Amqui
Amqui – miasto w Kanadzie , w prowincji Quebec , w regionie Bas-Saint-Laurent i MRC La Matapédia .
Miasto położone jest u ujścia rzeki Humqui do Matapédii .
Amqui jest największym i najważniejszym miastem w dolinie Matapédii .
Okolice dzisiejszego miasta były zamieszkane już ok. 500 roku p.n.e. , ale pierwsi osadnicy europejscy ( franko-kanadyjscy ) dotarli tutaj dopiero w 1848 .
W 1889 roku powstała tutaj parafia Saint-Joseph-Labre .
Miasto Amqui powstało w 1991 roku w wyniku połączenia parafii Saint-Joseph-Labre i wsi Amqui .
Liczba mieszkańców Amqui wynosi 6 261 .
Język francuski jest językiem ojczystym dla 99,5 % , angielski dla 0,2 % mieszkańców ( 2006 ) .
