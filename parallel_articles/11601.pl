Tavistock
Tavistock miasto w Wielkiej Brytanii , w Anglii w hrabstwie Devon nad rzeką Tavy , na skraju wrzosowiska Dartmoor .
Lokalny ośrodek handlowy i edukacyjny .
Historia .
Pierwsza wzmianka o mieście pochodzi z roku 961 – założono wtedy opactwo Tavistock .
Wykopaliska archeologiczne poświadczają istnienie w tym miejscu osad z epoki brązu .
W średniowieczu miasto było dużym centrum handlu o znaczeniu ponadregionalnym .
Postaci fikcyjne .
Pies Baskerville ’ ów , bohater głośnego opowiadania Artura Conana Doyle ’ a , pochodzi z bagien w pobliżu Tavistock .
