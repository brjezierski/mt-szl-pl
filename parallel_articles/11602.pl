Taylor Swift ( album )
Taylor Swift – debiutancki album studyjny amerykańskiej piosenkarki country i autorki tekstów Taylor Swift .
Album został wydany 24 października 2006 przez Big Machine Records .
Swift napisała piosenki na album podczas pierwszego roku nauki w liceum , a niektóre z nich napisała m.in .
z Liz Rose .
Producentem albumu został Nathan Chapman .
Muzycznie i lirycznie album jest stylizowany na muzykę country , piosenki mówią o romantycznych związkach , wśród których znalazły się takie , które Swift obserwowała .
Album został dobrze odebrany przez krytyków , którzy chwalili talent Swift .
Album odniósł komercyjny sukces : w Stanach Zjednoczonych zajął piąte miejsce w notowaniu `` Billboard 200 '' , znalazł się na liście `` Top Country Albums '' i pozostał na niej przez 24 tygodnie z rzędu , a także został uznany pięciokrotnie platynowym przez RIAA .
Krążek znajdował się najdłużej na liście `` Billboard 200 '' wśród wszystkich albumów wydanych w tej dekadzie .
Znalazł się też na listach przebojów m.in .
w Australii , Kanadzie i Wielkiej Brytanii .
