Tałdykorgan
Tałdykorgan ( kaz .
`` Талдықорған '' ) – miasto w południowo-wschodnim Kazachstanie , nad rzeką Karatał , u podnóża Ałatau Dżungarskiego , dawniej stolica obwodu tałdykurgańskiego , w latach 2001–2022 – stolica obwodu ałmackiego , od 2022 roku – stolica obwodu żetysuskiego .
W 2021 roku miasto zamieszkiwało niecałe 148 tys .
mieszkańców .
Transport .
Na północ od miasta znajduje się międzynarodowy port lotniczy Tałdy Kurgan .
Sport .
W mieście swoją siedzibę ma klub piłkarski Żetysu Tałdykorgan , grający w kazachskiej Priemjer-Liga i rozgrywający spotkania na własnym stadionie .
