Tczew
Tczew ( koc .
`` Derszewo '' , , , ) – miasto w północnej Polsce , w województwie pomorskim , siedziba powiatu tczewskiego , należące do aglomeracji trójmiejskiej .
W latach 1975–1998 miasto administracyjnie należało do województwa gdańskiego .
Według danych GUS z 30 czerwca 2021 r. , Tczew liczył 59 105 mieszkańców i był pod względem liczby ludności czwartym ( po Gdańsku , Gdyni oraz Słupsku ) miastem w województwie pomorskim , a także 73. spośród najludniejszych miast w Polsce .
Położenie .
Miasto położone na Pojezierzu Starogardzkim w pradolinie rzeki Wisły , w regionie Kociewie , drugim obok Kaszub regionie Pomorza Gdańskiego , w odległości ok. 30 km od Gdańska , należące do aglomeracji trójmiejskiej .
Jest jednym z najstarszych miast na Pomorzu , jego historia sięga 1198 roku .
Jest to węzeł komunikacyjny Polski północnej , leżący przy skrzyżowaniu szlaku Śląsk – Bałtyk i Berlin – Królewiec .
Krzyżują się tu droga krajowa nr 91 relacji Gdańsk – Toruń oraz droga wojewódzka nr 224 relacji Tczew – Wejherowo ( oznaczona drogowskazem Kościerzyna ) , a w pobliżu przebiega autostrada A1 – węzeł Stanisławie oraz Swarożyn , droga krajowa nr 22 Kostrzyn nad Odrą – Grzechotki oraz - w odległości ok. 20 km na północny wschód miasta - droga ekspresowa S7 .
Łącznie w Tczewie jest około 130 km dróg .
Sąsiednie gminy to Lichnowy , Miłoradz , Tczew ( gmina wiejska ) .
