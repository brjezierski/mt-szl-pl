Tecumseh
Tecumseh ( `` Tecumthe '' , `` Tikamthe '' , `` Tecumtha '' - j.shawnee `` Puma gotowa do skoku '' , `` Skacząca Puma '' , ur .
1768 , zm .
5 października 1813 ) – wódz północnoamerykańskiego plemienia indiańskiego Szaunisów ( Szawanezów ) , przywódca wojskowy , mówca , polityk , generał brytyjski .
Na początku XIX w. był zwolennikiem utworzenia szerokiej konfederacji plemion indiańskich na południe od Wielkich Jezior , której celem byłoby powstrzymanie inwazji białych .
