Tego Calderón
Tegui Calderón Rosario ( ur .
1 lutego 1972 w Santurce ) – portorykański raper , piosenkarz i aktor .
Najbardziej znany jest jako Tego Calderón lub pod pseudonimem „ El Abayarde ” , które odnosi się do malutkiej , wolno poruszającej się mrówki ognistej znalezionej w Portoryko .
Imię odnosi się do jego zachowania jako dziecka ; złośliwy i kłopotliwy .
Nagrywa także inne rodzaje muzyki takie jak mambo , blues , salsa ( np .
„ Planté bandera ” na swoim debiutanckim albumie oraz „ Llora , Llora ” i „ Llámame ” z Oscarem D'León ) w 2006 , blues ( „ Mardi Gras ” na albumie z 2006 ) i reggaeton .
Ponadto stworzył również utwory , które są czystym reggae .
Dzięki albumowi `` El Abayarde '' ( 2002 ) stał się gwiazdą wśród Latynosów .
