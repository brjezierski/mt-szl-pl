Tegucigalpa
Tegucigalpa ( `` Tegusyjgalpa '' ) – stolica i największe miasto Hondurasu , zamieszkuje je 1,13 mln osób ( 2012 ) .
Położone jest w południowej części kraju , u stóp góry Picacho , w dolinie rzeki Choluteca na wysokości ok. 975 m n.p.m .
Tegucigalpa jest również ośrodkiem administracyjnym departamentu Francisco Morazán .
Nazwa „ tegucigalpa ” oznacza w miejscowym indiańskim narzeczu „ srebrna góra ” .
Miasto zostało założone w 1578 roku , stanowiło wówczas ośrodek wydobycia srebra i złota .
Po uzyskaniu niepodległości przez Honduras w 1821 roku status stolicy dzieliło z pobliskim miastem Comayagua ( w latach 1823–1839 Honduras stanowił część Zjednoczonych Prowincji Ameryki Środkowej ) .
Dopiero w 1880 , na mocy decyzji prezydenta Marco Aurelio Soto Tegucigalpa stała się jedyną stolicą Hondurasu .
