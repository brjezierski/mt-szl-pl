Teheran
Teheran ( `` Tehran '' ) – stolica i największe miasto Iranu , będące również centrum administracyjnym prowincji o tej samej nazwie .
Teheran leży u stóp masywu górskiego Elburs i rozciąga się na wysokości od około 1100 m n.p.m .
do 1800 m n.p.m. , zajmując obszar 658 kilometrów kwadratowych .
Teheran jest największym miastem Bliskiego Wschodu i liczy 12,2 mln mieszkańców ( 2012 ) , zaś aglomeracja – 16,1 mln mieszkańców ( 2012 ) .
Posiada też gęstą sieć dróg i autostrad , znaczący dla kraju węzeł kolejowy oraz dwa porty lotnicze .
Na terenie miasta ulokowane są liczne muzea , centra sztuki , kompleksy pałacowe i centra kulturalne , a w promieniu kilkudziesięciu kilometrów – znane ośrodki narciarskie .
Ponad połowa irańskiego przemysłu znajduje się w Teheranie .
Stanowią go fabryki samochodów , podzespołów elektronicznych , urządzeń elektrycznych , przemysłu zbrojeniowego , włókienniczego , cukrowego , cementowego i chemicznego .
Jest to również centrum sprzedaży dywanów i mebli .
W pobliżu Teheranu znajduje się także rafineria .
Na terenie miasta leży najdłuższa ulica Bliskiego Wschodu – Waliasr – ciągnąca się z centrum Teheranu na północ przez ok. 20 km .
Na jej trasie znajduje się wiele dużych placów takich jak : plac Waliasr , plac Wanak , plac Tadżrisz – pełnią one rolę punktów odniesienia .
W XX wieku Teheran przyjął wielką emigrację ludzi z terenu całego Iranu zróżnicowanych zarówno etnicznie , jak i religijnie .
Miasto jest wypełnione historycznymi meczetami , kościołami , synagogami i zoroastriańskimi świątyniami ognia .
Historia .
Początki historii Teheranu nie są znane .
Przez długi czas miasto rozwijało się w cieniu dużo większego ośrodka miejskiego Rej , ważnego ośrodka na Jedwabnym Szlaku , zniszczonego w XIII wieku przez Mongołów .
Prawdopodobnie pierwszym Europejczykiem odwiedzającym Teheran był Ruy Gonzáles de Clavijo , kastylijski ambasador podróżujący do Samarkandy , ówczesnej stolicy Timurydów .
Teheran został stolicą Persji w dniu koronacji Aghi Mohammada w 1795 roku i pozostaje nią nieprzerwanie do dziś .
W czasie II wojny światowej Teheran został zajęty przez wojska brytyjskie i sowieckie .
