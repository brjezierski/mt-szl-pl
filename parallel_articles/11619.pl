należą do 5 największych aglomeracji w Stanach Zjednoczonych .
Stolicą jest Austin .
Baza przemysłowa Teksasu przyciągnęła w ostatnich dziesięcioleciach wiele firm i pracowników ze względu na łagodne regulacje i niskie podatki – jest to jeden z nielicznych stanów bez podatku dochodowego od osób fizycznych .
Geografia .
Ukształtowanie powierzchni przeważnie równinne i wyżynne , w środkowej części rozległa Wyżyna Edwardsa , należąca do Wielkich Równin , na północnym zachodzie równina Llano Estacado , na południowym wschodzie Nizina Zatokowa ; na południowym zachodzie skrawek Gór Skalistych , z najwyższym szczytem Guadalupe Peak , 2667 m n.p.m .
