Religia .
Struktura religijna w 2014 r. :
W 2010 roku Kościół katolicki pozostaje największą pojedynczą organizacją religijną w Teksasie .
Hrabstwa rzymskokatolickie są przede wszystkim skupione w południowym Teksasie wzdłuż granicy z Meksykiem .
Im bardziej na północ tym większa przewaga protestantów .
Do największych ewangelikalnych kościołów ( z ponad 300 tys .
członków ) należą : Południowa Konwencja Baptystyczna , lokalne kościoły bezdenominacyjne ( np .
Kościół Lakewood ) , Kościoły zielonoświątkowe i Kościoły Chrystusowe .
Protestantyzm głównego nurtu reprezentowany jest głównie przez Zjednoczony Kościół Metodystyczny , który jest trzecią co do wielkości organizacją religijną w Teksasie .
Teksas ma największą społeczność muzułmańską w USA liczącą 422 tysiące wyznawców , oraz piątą co do wielkości społeczność mormońską liczącą ponad 300 tys .
