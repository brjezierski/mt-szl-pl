Tel Awiw-Jafa
Tel Awiw-Jafa ( , trl .
`` Tel Awiw-Jafo '' , trb .
`` Tel Awiw-Jafo '' ; trl .
`` Tall Abīb-Yāfā '' , trb .
`` Tall Abib-Jafa '' ) , zwyczajowo nazywane Tel Awiw – drugie pod względem wielkości miasto Izraela .
Miasto jest położone w Dystrykcie Tel Awiwu na nadmorskiej równinie Szaron leżącej nad Morzem Śródziemnym .
Tel Awiw zajmuje powierzchnię 51,8 km² , będąc największym i najludniejszym miastem obszaru metropolitalnego Gusz Dan .
Miasto jest zarządzane przez władze miejskie Tel Awiwu-Jafy , na czele których stoi burmistrz Ron Chuldaj .
Tel Awiw został założony w 1909 na peryferiach starożytnego portu morskiego Jafa ( hebr .
יָפוֹ ; arab .
يافا ) .
Duże tempo rozwoju Tel Awiwu bardzo szybko pozostawiło Jafę z tyłu , która wyraźnie zatrzymała się w arabskiej przeszłości .
W dwa lata po powstaniu państwa Izrael , w 1950 Tel Awiw i Jafa zostały połączone w jedno miasto .
Choć stolicą proklamowaną przez Izrael jest Jerozolima , to ONZ nie uznaje tego faktu , dlatego większość ambasad mieści się w Tel Awiwie .
Zespół miejski Białego Miasta Tel Awiwu został umieszczony w 2003 na liście światowego dziedzictwa UNESCO , jako największe na świecie skupisko budynków modernistycznych .
W grudniu 2017 prezydent Stanów Zjednoczonych , Donald Trump , oficjalnie uznał Jerozolimę za stolicę Izraela i wydał Departamentowi Stanu polecenie rozpoczęcia procedury przenoszenia amerykańskiej ambasady do tego miasta z Tel Awiwu , na mocy uchwalonej w 1995 ustawy o ambasadzie w Jerozolimie .
Tel Awiw jest sklasyfikowany jako metropolia globalna .
