Telsze ( stacja kolejowa )
Telsze ( , ) – stacja kolejowa w miejscowości Telsze , w rejonie telszańskim , w okręgu telszańskim , na Litwie .
Położona jest na linii Szawle – Kretynga .
Historia .
Stacja została otwarta w 1926 , jako stacja krańcowa linii z Kužiai ( Szawli ) .
W 1930 wybudowano linię na zachód od Telsz , do Kretyngi .
