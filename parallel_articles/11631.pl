Tema leży 25 km na wschód od Akry .
Było małą wioską rybacką do roku 1960 , rozwój nastąpił w wyniku budowy w miejscowości głównego portu morskiego Ghany .
Jest ośrodkiem przetwórstwa ropy naftowej i ośrodkiem przemysłowym .
Historia .
Po roku 1950 Takoradi i inne starsze porty pełnomorskie Ghany nie były w stanie obsłużyć zwiększającego się handu zagranicznego .
Budowa drugiego portu pełnomorskiego okazała się niezbędna , szczególnie dla wschodniej części kraju i dorzecza Wolty .
W roku 1951 na miejsce budowy wybrano Temę w związku z jej położeniem blisko rzek Wolta i Akra , miejscowych rezerw materiałów wykorzystanych do budowy falochronów oraz głębokiej wody daleko od brzegu , które minimalizowało roboty przy pogłębianiu dna .
Port otwarty został formalnie w roku 1962 .
