Tendais ( Cinfães )
Tendais – parafia `` ( freguesia ) '' w gminie Cinfães , w Portugalii .
Według danych na rok 2011 parafię zamieszkiwały 697 osoby , a gęstość zaludnienia wyniosła 21,9 os./km2 .
Klimat .
Średnia temperatura wynosi 13 °C .
Najcieplejszym miesiącem jest sierpień ( 22 °C ) , a najzimniejszym styczeń ( 5 °C ) .
Średnia suma opadów wynosi 1284 milimetrów rocznie .
Najbardziej wilgotnym miesiącem jest styczeń ( 181 milimetrów opadów ) , a najbardziej suchym jest sierpień ( 12 milimetrów opadów ) .
