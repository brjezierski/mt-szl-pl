Popielatek garbaty
Popielatek garbaty , kępkowiec garbaty ( `` Tephrocybe ambusta '' ( Fr . )
Donk ) – gatunek grzybów należący do rodziny kępkowcowatych ( `` Lyophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tephrocybe , Lyophyllaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1821 r. Elias Fries nadając mu nazwę `` Agaricus umbratilis ß ambustus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Marinus Anton Donk w 1962 r .
Synonimy nazwy naukowej :
Polska nazwa popielatek dla rodzaju `` Tephrocybe '' występuje w pracy Barbary Gumińskiej i Władysława Wojewody z 1999 roku ( jest tam wymieniony popielatek bagieny `` Tephrocybe palustris '' ) .
W 2003 r. W. Wojewoda zaproponował nazwę kępkowiec garbaty , jest ona jednak niespójna z aktualną nazwą naukową .
Nazwa popielatek garbaty występuje m.in .
w atlasie grzybów Pavola Škubli .
