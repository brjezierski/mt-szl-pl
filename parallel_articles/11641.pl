Tepic ( gmina )
Tepic – gmina w środkowej części meksykańskiego stanu Nayarit , położona w odległości około 50 km od wybrzeża Oceanu Spokojnego .
Jest jedną z 20 gmin w tym stanie .
Siedzibą władz gminy jest miasto Tepic .
Populacja gminy Tepic wynosiła w 2010 roku 380 249 osób , co czyni ją najliczebniejszą gminą w stanie Nayarit .
Główne grupy etniczne gminy to Huiczole , Cora , Tepehuán i potomkowie Azteków posługujący się językiem nahuatl .
