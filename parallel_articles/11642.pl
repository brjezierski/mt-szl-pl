`` Divan '' , dawniej `` Velin '' , `` Welin '' ) – śródleśna osada kaszubska w Polsce położona w województwie pomorskim , w powiecie kościerskim , w gminie Dziemiany w kompleksie leśnym Borów Tucholskich nad rzeką Zbrzycą w pobliżu jeziora Dywańskiego ( Do września 1939 roku przez jezioro na zachód od osady przebiegała granica polsko-niemiecka ) .
Osada wchodzi w skład sołectwa Trzebuń .
W latach 1975–1998 miejscowość położona była w województwie gdańskim .
