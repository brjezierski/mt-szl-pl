Cieplice ( gromada )
Cieplice – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Cieplice z siedzibą GRN w Cieplicach utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie jarosławskim w woj .
rzeszowskim na mocy uchwały nr 21/54 WRN w Rzeszowie z dnia 5 października 1954 .
W skład jednostki weszły obszary dotychczasowych gromad Cieplice i Słoboda ze zniesionej gminy Adamówka w tymże powiecie .
Dla gromady ustalono 10 członków gromadzkiej rady narodowej .
