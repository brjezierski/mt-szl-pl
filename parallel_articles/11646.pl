Tequixquiac
Tequixquiac – gmina leżąca w stanie Meksyk w Meksyku , na północ od miasta Meksyk , powstała w 1820 roku .
Siedzibą władz gminy jest Santiago Tequixquiac .
Graniczy z gminami stanu Meksyk : Apaxco ( na północy ) , Hueypoxtla ( na wschodzie ) , Zumpango ( na południu ) , Huehuetoca oraz na zachodzie z Atotonilco de Tula gminą stanu Hidalgo .
Pochodzenie nazwy .
Nazwa pochodzi z języka nahuatl i oznacza w dosłownym tłumaczeniu `` Saletrzana woda '' ( `` Tequexquitl '' =saletra , `` atl '' =woda ) .
