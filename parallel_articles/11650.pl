Teratomyces philonthi
Teratomyces philonthi Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Teratomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1900 r. Roland Thaxter na owadach z rodzaju `` Philonthus '' na Węgrzech .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w 1994 r. opisał jego występowanie na chrząszczu `` Gabrius appendiculatus '' z rodziny kusakowatych ( Staphylinidae ) .
