Terespol ( obwód grodzieński )
Terespol ( ; ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie świsłockim , w sielsowiecie Nowy Dwór , na terenie Parku Narodowego „ Puszcza Białowieska ” .
W dwudziestoleciu międzywojennym leżał w Polsce , w województwie białostockim , w powiecie wołkowyskim , w gminie Porozów .
