Obwód tarnopolski
Obwód tarnopolski ( , `` Ternopilśka obłast´ '' ) – jeden z 24 obwodów Ukrainy .
Leży w zachodniej części Ukrainy .
Stolicą obwodu jest Tarnopol .
Graniczy na północy z obwodem rówieńskim , na południowym zachodzie z iwanofrankiwskim , na południu z czerniowieckim , na zachodzie z lwowskim , na wschodzie z chmielnickim .
Obwód został utworzony 4 grudnia 1939 r. dekretem Prezydium Rady Najwyższej ZSRR z części terytorium II Rzeczypospolitej okupowanego przez Armię Czerwoną w trakcie agresji ZSRR na Polskę i anektowanego następnie przez ZSRR .
Geograficznie leży na Wyżynie Podolskiej , historycznie na obszarze Wołynia ( północ ) , Rusi Czerwonej ( centrum ) i Podola ( południowy wschód ) .
