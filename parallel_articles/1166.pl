Komunikacja .
Przez całą długość gminy przebiega droga krajowa B195 pomiędzy Dömitz a Boizenburg/Elbe .
Bardzo ważnym szlakiem komunikacyjnym jest Łaba .
Łączność z powiatem utrzymywana jest przez przeprawy promowe lub przez most na Łabie koło Dömitz ( droga krajowa B191 ) .
