Amt Neuhaus
Amt Neuhaus – gmina samodzielna ( niem .
`` Einheitsgemeinde '' ) położona w niemieckim kraju związkowym Dolna Saksonia , w powiecie Lüneburg .
Położenie geograficzne .
Amt Neuhaus leży we wschodniej części powiatu Lüneburg na prawym brzegu Łaby ok. 35 km .
na wschód od Lüneburga .
Jest jedyną gminą nie tylko powiatu , ale i całej Dolnej Saksonii położoną po prawej stronie Łaby .
( Są jeszcze dwie małe miejscowości `` Neu Bleckede '' i `` Neu Wendischthun '' należące do miasta Bleckede , które również leżą po prawej stronie Łaby ) .
Amt Neuhaus graniczy od północy i wschodu z krajem związkowym Meklemburgia-Pomorze Przednie ; w tym z gminami Besitz , Brahlstorf , Teldau i Dersenow wchodzącymi w skład urzędu Boizenburg-Land na północy , z miasteczkiem Lübtheen oraz z gminami z urzędu Dömitz-Malliß Vielank i Dömitz na wschodzie .
Na południu graniczy na Łabie z miastami Hitzacker ( Elbe ) i Dannenberg ( Elbe ) z gminy zbiorowej Elbtalaue w powiecie Lüchow-Dannenberg .
Jedynie na zachodzie i północnym zachodzie Amt Neuhaus ma kontakt z powiatem Lüneburg przez miasto Bleckede .
Przez gminę płyną jeszcze poza Łabą również inne rzeki : Sude i jej dopływy Rögnitz i Krainke .
Historia .
Od początków kolonizowania tych terenów zamieszkałych przez słowiańskich Drzewian przez Sasów należał Neuhaus do księstwa Saksonia-Lauenburg .
W XVII wieku zostały przejęte te tereny przez księstwo Hanoweru a w XIX wieku przez Prusy .
Po II wojnie światowej został Amt Neuhaus przyłączony do NRD , mimo że historycznie należał przez wiele wieków do Dolnej Saksonii .
Było to spowodowane praktycznymi racjami np .
brak mostów przez Łabę do Dolnej Saksonii po lewej stronie rzeki .
Ten niefortunny podział został poprawiony po przyłączeniu NRD do Republiki Federalnej Niemiec .
Od 30 czerwca 1993 Amt Neuhaus należy znowu do Dolnej Saksonii .
Podział administracyjny .
W skład gminy Amt Neuhaus wchodzi 40 dzielnic , z czego najważniejsze uznane w oficjalnym podziale gminy to : `` Dellien '' , `` Haar '' , `` Kaarßen '' , `` Neuhaus '' , `` Stapel '' , `` Sumte '' i `` Tripkau '' .
Inne wchodzą w skład tych wymienionych .
