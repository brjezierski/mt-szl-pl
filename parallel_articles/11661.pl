Terre Haute
Terre Haute – miasto w Stanach Zjednoczonych , w stanie Indiana , nad rzeką Wabash .
Liczy nieco ponad 60 tysięcy mieszkańców .
W Terre Haute ma swój główny kampus Uniwersytet Stanu Indiana ( ang .
`` Indiana State University '' ) .
W mieście rozwinął się przemysł papierniczy , chemiczny , spożywczy oraz fonograficzny .
