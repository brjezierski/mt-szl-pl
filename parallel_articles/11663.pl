Terry Pratchett
Terry Pratchett , właśc .
sir Terence David John Pratchett ( ur .
28 kwietnia 1948 w Beaconsfield , zm .
12 marca 2015 w Broad Chalke ) – brytyjski pisarz fantasy i science fiction , najbardziej znany jako autor cyklu `` Świat Dysku '' .
Inne jego dzieła to m.in .
`` Trylogia Johnny ’ ego Maxwella '' i `` Trylogia Nomów '' .
Współpracował także przy adaptacjach swojej twórczości na potrzeby sztuk teatralnych i gier komputerowych .
Jego pierwsza powieść `` Dywan '' została opublikowana w 1971 .
Pierwsza książka z serii `` Świat Dysku '' – `` Kolor magii '' – ukazała się w 1983 i od tego momentu autor kończył średnio dwie książki rocznie .
Książki Pratchetta były w latach 90. najlepiej sprzedającymi się książkami w Wielkiej Brytanii .
Do lutego 2007 roku sprzedał na całym świecie ok. 50 milionów książek , a jego dzieła zostały przetłumaczone na 33 języki .
Obecnie jest na drugim miejscu listy najchętniej czytanych pisarzy w Wielkiej Brytanii , a w USA na siódmym wśród pisarzy niepochodzących ze Stanów Zjednoczonych .
Terry Pratchett w 1998 „ za zasługi dla literatury ” został odznaczony Orderem Imperium Brytyjskiego w klasie `` Officer '' .
Jego powieść `` Zadziwiający Maurycy i jego edukowane gryzonie '' zdobyła w 2001 nagrodę Carnegie Medal za najlepszą książkę dla dzieci .
