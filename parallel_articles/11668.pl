Teuchern
Teuchern − miasto w Niemczech , w kraju związkowym Saksonia-Anhalt , w powiecie Burgenland .
Do 30 czerwca 2007 miasto należało do powiatu Weißenfels .
Do 31 grudnia 2010 było siedzibą wspólnoty administracyjnej Vier Berge-Teucherner Land , która w wyniku reformy administracyjnej została rozwiązana , a gminy Deuben , Gröben , Gröbitz , Krauschwitz , Nessa , Prittitz i Trebnitz wraz z miastem Teuchern utworzyły nowe miasto , o tej samej nazwie .
Geografia .
Teuchern leży na południe od Weißenfels .
1 stycznia 2011 Bonau , Lagnitz , Schelkau , Schortau i Vorwerk Lagnitz stały się częścią Teuchern .
