Texarkana ( Teksas )
Texarkana – miasto w Stanach Zjednoczonych , w stanie Teksas , w hrabstwie Bowie .
Nazwa , podobnie jak bliźniaczej Texarkany , położonej po drugiej stronie stanowej granicy , pochodzi od nazw stanów Teksas , Arkansas i Luizjana .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 miasto liczyło 36 411 mieszkańców , co oznacza wzrost o 4,7 % w porównaniu do roku 2000 .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 55,4 % , Afroamerykanie 37,1 % , Azjaci 1,3 % , pozostali 6,2 % .
Phantom Killer .
Między 22 lutego a 3 maja 1946 roku w okolicach Teksarkany zamordowanych zostało 5 osób .
Morderca zwany „ Phantom Killer ” nie został nigdy złapany .
O morderstwach nakręcono w 1976 roku film : The Town That Dreaded Sundown `` ( Miasteczko , które bało się zmierzchu ) '' , który doczekał się remake ’ u w 2014 .
