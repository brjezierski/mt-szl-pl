Skórniczek świerkowy
Skórniczek świerkowy , skórniczek szczelinowaty ( `` Amylostereum areolatum '' ( Chaillet ex Fr . )
Boidin ) – gatunek grzybów należący do rodziny `` Echinodontiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Stereum , Echinodontiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1828 r. Jean Frédéric de Chaillet i Elias Fries nadając mu nazwę `` Thelephora areolata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1958 r. Jacques Boidin , przenosząc go do rodzaju `` Amylostereum '' .
Synonimy :
Stanisław Domański opisał ten gatunek pod nazwą skórniczek szczelinowaty , Barbara Gumińska i Władysław Wojewoda w 1983 r. podali nową nazwę – skórniczek świerkowy .
