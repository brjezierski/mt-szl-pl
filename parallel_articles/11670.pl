Tezpur
Tezpur ( assamski : তেজপুৰ ) – miasto w północno-wschodnich Indiach , stolica dystryktu Sonitpur w stanie Asam , nad Brahmaputrą .
Około 59.000 mieszkańców .
Pochodzenie nazwy .
Nazwa Tezpur pochodzi o sanskryckich słów `` Teza '' − krew i `` Pura '' − miasto .
Według legendy poprzednia nazwa brzmiała Sonitpur , jednak podczas bitwy między armiami Kryszny i Banasury przelano tyle krwi , że całe miejsce zabarwiło się na czerwono i to zdarzenie spowodowało zmianę nazwy na obecnie używaną .
