Tešanj
Tešanj – miasto w Bośni i Hercegowinie , w Federacji Bośni i Hercegowiny , w kantonie zenicko-dobojskim , siedziba gminy Tešanj .
W 2013 roku liczyła 5257 mieszkańców , z czego większość stanowili Boszniacy .
Historia .
Pierwsze wzmianki o Tešanju pochodzą z 1461 r. , choć twierdza powstała wcześniej , być może ok. III w. p.n.e .
Miasto zostało zniszczone przez Eugeniusza Sabaudzkiego podczas walk przeciwko Turkom .
