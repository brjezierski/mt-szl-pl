Szydlina różowa
Szydlina różowa ( `` Thamnolia vermicularis '' ( Sw. ) Schaer .
– gatunek grzybów z rodziny czasznikowatych ( `` Icmadophilaceae '' ) .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Icmadophilaceae , Pertusariales , Ostropomycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1781 r. Olof Swartz nadając mu nazwę `` Lichen vermicularis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1850 r. Ludwig Emanuel Schaerer , przenosząc go do rodzaju `` Thamnolia '' .
Niektóre synonimy naukowe :
Nazwa polska według `` Krytycznej listy porostów i grzybów naporostowych Polski '' .
Pochodzi od tego , że po dłuższym przechowywaniu w zielniku nabiera różowego koloru .
Morfologia .
