Rhizoctonia ochracea
Rhizoctonia ochracea ( Massee ) Oberw. , R. Bauer , Garnica & amp ; R. Kirschne ( strzępniczek ochrowy ) – gatunek grzybów z rodziny podstawkorożkowatych ( `` Ceratobasidiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Rhizoctonia , Ceratobasidiaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1889 r. George Edward Massee , nadając mu nazwę `` Coniophora ochracea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Franz Oberwinkler , Robert Bauer , Sigisfredo Garnica i Roland Kirschne w 2013 r .
W 2003 r. Władysław Wojewoda zaproponował dla teleomorfy `` Thanatephorus ochraceus '' polską nazwę strzępniczek ochrowy .
Ponieważ jednak za nazwę naukową przyjęto nazwę anamorfy , polska nazwa jest z nią niespójna .
