The Corrs
The Corrs – irlandzki zespół muzyczny , grający muzykę z pogranicza popu , rocka i folku .
Tworzy go rodzeństwo – trzy siostry i brat : Andrea Corr ( wokal , flażolet irlandzki ) , Caroline Corr ( perkusja , bodhran , chórki ) , Sharon Corr ( skrzypce , chórki ) i Jim Corr ( klawisze , gitara ) .
Wszyscy członkowie zespołu potrafią grać ( w różnym stopniu zaawansowania ) na fortepianie .
Gry na tym instrumencie uczył ich ojciec , Gerry Corr .
Historia .
Urodzili się w Irlandii , w Dundalk , w hrabstwie Louth .
Zespół został założony w 1990 roku , przez najstarszego z rodzeństwa – Jima , który wcześniej występował w innych kapelach .
W kwietniu 1994 roku rodzeństwo zostało zauważone podczas gry w dublińskim barze przez amerykańskiego ambasadora , któremu zespół bardzo przypadł do gustu .
Dlatego zaprosił ich do Stanów Zjednoczonych , gdzie The Corrs zagrali na otwarciu Mistrzostw Świata w piłce nożnej w Bostonie .
Spotkali się z Davidem Fosterem , który był pod dużym wrażeniem zespołu .
We wrześniu 1995 roku wyszedł pierwszy album The Corrs pt .
`` Forgiven Not Forgotten . ''
Album osiągnął ponad dwumilionową sprzedaż , a w Irlandii stał się jednym z najlepiej sprzedających się albumów .
Zespół utorował sobie drogę dla swojej debiutanckiej płyty , grając jako support podczas trasy koncertowej Céline Dion na Igrzyskach Olimpijskich w Atlancie .
Po tych sukcesach , zespół w październiku 1997 roku wydał drugi album pt .
`` Talk On Corners '' , dzięki któremu stał się znany na całym świecie .
The Corrs wyruszyli w pierwszą trasę koncertową .
Byli zaskoczeni swoją wielką popularnością , zwłaszcza w Irlandii .
Jednak największą sławę przyniósł album pt .
`` In Blue '' , wydany w lipcu 2000 roku .
29 listopada 2003 zagrali na charytatywnym koncercie zorganizowanym pod patronatem Nelson Mandeli w ramach akcji `` 46664 '' na rzecz chorych na AIDS .
Pod koniec maja 2004 roku wyszedł ich czwarty album pt .
`` Borrowed Heaven '' , zadedykowany zmarłej matce Jean i ojcu Gerry'emu .
Na płycie znalazł się utwór `` Time Enough for Tears '' , którego współautorem jest Bono .
2 lipca 2005 zespół wziął udział w koncercie w Edynburgu kończącym akcję Live 8 .
26 sierpnia 2005 ukazała się ich kolejna płyta pt .
`` Home '' .
