The Doors ( album )
The Doors – debiutancki album studyjny amerykańskiego zespołu rockowego The Doors , wydany 4 stycznia 1967 .
Pochodzą z niego przeboje „ Light My Fire ” oraz „ The End ” .
Sprzedał się w liczbie 20 milionów egzemplarzy i został wprowadzony do Grammy Hall of Fame .
W 2012 roku został sklasyfikowany na 42. miejscu listy 500 albumów wszech czasów magazynu „ Rolling Stone ” .
W 2015 roku Biblioteka Kongresu wybrała `` The Doors '' do włączenia do Krajowego Rejestru Zapisów w oparciu o jego kulturowe , artystyczne lub historyczne znaczenie .
Lista utworów .
Teksty utworów zostały napisane przez wszystkich czterech członków zespołu , z wyjątkiem „ Alabama Song ( Whiskey Bar ) ” ( Bertolt Brecht i Kurt Weill ) oraz „ Back Door Man ” ( Willie Dixon ) .
