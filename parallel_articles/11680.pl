The Hooters
The Hooters – amerykański zespół rockowy z Filadelfii , działający w latach 1980–1995 , reaktywowany w 2001 .
Znany z przebojów takich jak „ All You Zombies ” , „ Johnny B ” , „ And We Danced ” , „ 500 Miles ” , „ Satellite ” i „ Private Emotion ” .
Początkowe lata funkcjonowania grupy ograniczały się do występów w klubach i stacjach radiowych .
W 1983 The Hooters wydali swój pierwszy album `` Amore '' , zawierający oryginalną wersję „ All You Zombies ” , który sprzedał się w około 100 tys .
