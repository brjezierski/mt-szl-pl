Zespół współpracował następnie z Cyndi Lauper przy nagraniu singla „ Time After Time ” .
Druga połowa lat 80. to okres największych sukcesów grupy , zapoczątkowany platynowym statusem ich drugiej płyty `` Nervous Night '' .
W 1995 The Hooters zawiesili swoją działalność na następne sześć lat .
W 2001 doszło do reaktywacji zespołu .
21 listopada tego samego roku grupa zagrała jeden koncert w Filadelfii na 20-lecie lokalnego radia WMMR z Pensylwanii .
W 2003 odbyła liczącą 17 występów trasę koncertową na terenie Niemiec , następnie w 2004 i 2005 kolejne dwie trasy po Niemczech , Szwajcarii oraz Szwecji .
W czerwcu 2006 odbyło się pierwsze po reaktywacji tournée grupy w Stanach Zjednoczonych .
W drugiej połowie 2006 grupa zaczęła nagrywać nowy materiał , pierwszy od wydania w 1993 `` Out of Body '' .
Nowy album , `` Time Stand Still '' , ukazał się we wrześniu 2007 .
