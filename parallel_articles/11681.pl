Geneza .
Popularność wcześniejszej powieści Tolkiena `` Hobbit '' ( 1937 ) spowodowała , że wydawcy poprosili pisarza o stworzenie kontynuacji .
Tolkien początkowo wysłał kilka gotowych już utworów ( przyjęto `` Rudego Dżila i jego psa '' , zaś `` Łazikanty '' i `` Silmarillion '' nie zostały skierowane do publikacji ) , ale pod naciskiem zaczął tworzyć nowe dzieło .
Pisaną przez ok. 12 lat ( 1937-1949 ) powieść wydano w Wielkiej Brytanii w latach 1954–1955 .
W połowie lat sześćdziesiątych `` Władca Pierścieni '' stał się fenomenem wydawniczym w krajach anglosaskich , a następnie na całym świecie .
Książkę przetłumaczono na dziesiątki języków ( w Polsce ukazała się w latach 1961–1963 ) .
