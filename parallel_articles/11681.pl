Władca Pierścieni
Władca Pierścieni ( ang .
`` The Lord of the Rings '' ) – powieść `` high fantasy '' J.R.R .
Tolkiena , której akcja rozgrywa się w mitologicznym świecie Śródziemia .
Jest ona kontynuacją innej powieści tego autora zatytułowanej `` Hobbit , czyli tam i z powrotem '' .
Sześć ksiąg .
`` Władca Pierścieni '' jest często mylnie nazywany trylogią , choć w rzeczywistości jest to powieściowa całość , podzielona na sześć ksiąg .
Ze względu na swoją dużą objętość została wydana w trzech tomach z powodu nacisków wydawcy ; nie było to jednak oryginalne zamierzenie Tolkiena .
