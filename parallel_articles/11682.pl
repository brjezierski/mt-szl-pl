The Offspring ( album )
The Offspring – debiutancki album grupy The Offspring wydany w roku 1989 i wznowiony w 1995 .
Charakteryzuje się szybkim , surowym brzmieniem , porównywalnym do nagrań Dead Kennedys lub wcześniejszych Bad Religion .
Pierwotnie dostępny był tylko na dwunastocalowej płycie winylowej i kasecie .
Wersja z roku 1995 jest taka sama jak pierwotna .
Zmianie uległa jedynie wytwórnia – z Nemesis Records na Nitro Records ( label Dextera Hollanda ) .
Zmieniono również tylną okładkę , która w oryginale była bardzo brutalna ( przedstawiała potwora przypominającego ksenomorfa , wychodzącego z ciała ) .
Piosenki na płycie poruszają tematykę wojny ( `` Jenniffer Lost the War '' , `` Out on Patrol '' , `` Tehran '' ) , protest song przeciwko polityce wewnętrznej rządu Stanów Zjednoczonych ( `` Kill the President '' ) lub zbrojnej polityce zagranicznej ( `` Tehran '' ) , czy problem napięć w stosunkach między młodzieżą a dorosłymi ( `` Elders '' ) .
