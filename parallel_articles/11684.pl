The Prodigy
The Prodigy – brytyjski zespół muzyczny tworzący muzykę elektroniczną .
Muzyka zespołu stanowi pochodną wielu gatunków muzycznych .
Styl muzyczny formacji ewoluował począwszy od techno i rave we wczesnych latach 90 .
XX w. , po breakbeat , hardcore , dance , big beat , industrial i rock z elementami punkowej ekspresji wokalnej w latach późniejszych .
