The Velvet Underground ( album )
The Velvet Underground – trzeci album studyjny amerykańskiego zespołu rockowego The Velvet Underground wydany w marcu 1969 przez MGM Records .
Jest to pierwszy album muzyków nagrany bez Johna Cale ’ a i pierwszy nagrany z Dougiem Yulem .
Płyta była nagrywana w TTG Studios w Hollywood .
Album jest lżejszy od poprzednich .
`` The Velvet Underground '' składa się głównie z folk rockowych i popowych ballad .
W 2003 wydawnictwo zostało sklasyfikowane na 314 miejscu listy 500 albumów wszech czasów dwutygodnika „ Rolling Stone ” .
