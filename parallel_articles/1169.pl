An Dehai
An Dehai ( zm .
1869 ) , chiński polityk , główny eunuch Zakazanego Miasta w latach 1861–1869 .
Jeden z głównych współpracowników cesarzowej Cixi .
Asystował przy ceremonii wyboru monarszych żon i konkubin w 1851 , podczas której Jehonala została włączona do haremu .
Według jednej z relacji , podczas walki o władzę po śmierci cesarza Xianfenga dostarczył księciu Gongowi list wdów po władcy , wzywający go i księcia Chun do przyjazdu do Rehe ( Dżehol ) .
Kilka dni później , w tajemnicy przed regentami , zorganizował spotkanie spiskowców .
W 1861 , w uznaniu zasług , otrzymał stanowisko głównego eunucha .
Stał się jednym z najbliższych współpracowników cesarzowej Cixi , która powierzała mu organizację rozrywek w swoich apartamentach .
Jego pozycja na dworze była tak silna , że w obecności dworzan lżył drugą regentkę , Ci ’ an .
Zaostrzenie konfliktu między Cixi a księciem Gongiem w 1865 wpłynęło również na stosunek głównego eunucha do tego polityka .
Zaczął traktować go z pogardą , łamiąc również w jego obecności przyjęte etykietą normy .
Wykorzystując swoje wpływy zmusił arystokratę do podarowania mu cennego pierścienia .
Następnie zaś , mimo prośby Cixi by tego nie robił , chwalił się nim w obecności samego Gonga .
Miało to wywołać u poniżonego księcia krwi chęć zemsty .
Monarchini , pragnąc chronić swego protegowanego , postanowiła wyprawić go na jakiś czas ze stolicy .
Miał się udać nas południe Chin i w imieniu władczyni dokonać wyboru jedwabnych tkanin .
Było to sprzeczne z prawem dworskim , zgodnie z którym eunuchom nie było wolno opuszczać stolicy .
An Dehai wyruszył jednak w podróż , a towarzyszący mu orszak zajmował kilka bogato zdobionych łodzi .
W miastach , w których się zatrzymywał faworyt Cixi był przyjmowany jako cesarski wysłannik .
Razem z głównym eunuchem podróżował Ding Baozhen , gubernator prowincji Shandong .
Był on jego wrogiem , odkąd zmuszony został do przekazania mu 10 tys .
liangów srebra .
Podczas jednego z postojów wysłał do księcia Gonga posłańca z informacją o tym , że został złamany zakaz przebywania kastratów poza miastem stołecznym .
Wiadomość ta dotarła także do Ci ’ an , która postanowiła zlikwidować znienawidzonego dygnitarza .
Wydała edykt , w którym nakazała aresztowanie , a następnie stracenie go .
Rozkaz wykonano pod nadzorem Ding Baozhena .
Zwłoki An Dehaia pogrzebano na cmentarzu , na którym chowano żebraków .
