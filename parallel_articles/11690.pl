Theodor Erdmann Kalide
Theodor Erdmann Kalide ( ur .
8 lutego 1801 w Królewskiej Hucie , zm .
23 sierpnia 1863 w Gliwicach ) − niemiecki rzeźbiarz określany mianem
„ rzeźbiarza brązu i żelaza ” .
Życiorys .
Był drugim z kolei , po starszym o 6 lat Wilhelmie , synem niemieckiego inspektora hutniczego Gottlieba Kalide z miejscowej huty żelaza i Górnoślązaczki , pochodzącej z Nakła Śląskiego - Charlotty Wilhelminy Beck .
Miał jeszcze siostrę - Alwinę , przyszłą żonę Franza Winklera .
Przyszedł na świat w jednym z pierwszych budynków , tworzących osadę królewskohucką ( stawianych w latach 1798-1802 ) .
Edukację rozpoczął w szkole brackiej ( `` Knappschaftsschule '' ) w Królewskiej Hucie , następnie od 1816 uczęszczał do nowo utworzonego gimnazjum w Gliwicach .
Zamieszkiwał wówczas u pracującego w Królewskiej Odlewni Żeliwa w Gliwicach brata Wilhelma .
Szybko jednak przerwał naukę i już w 1817 r. podjął pracę jako elew ( uczeń ) w hucie „ Królewskiej '' , a następnie we wspomnianej Królewskiej Odlewni Żeliwa , jako pracownik modelarni .
Kształcił się pod okiem Friedricha Wilhelma Ludwiga Beyerhausa .
W 1819 udał się do Berlina , gdzie uczęszczał na zajęcia do Królewskiej Akademii Sztuk Pięknych , najpierw jako wolny słuchacz , a następnie jako student .
Pobierał nauki u wybitnych mistrzów : Johanna Gottfrieda Schadowa i Chrystiana Daniela Raucha .
Już w latach 20 .
XIX wieku Kalide zyskał sławę plastyka postaci zwierzęcych , szczególnie dzięki umiejętności tworzenia naturalnie ukształtowanych modeli śpiących i czuwających lwów .
Królewska Odlewnia Żeliwa w Gliwicach , jako fundator stypendium , zawarła w 1824 z Kalide nowy kontrakt , w którym zobowiązał się on do wykonywania i posyłania z Berlina do odlewni w Gliwicach modeli swoich prac , według których można było wykonywać formy odlewnicze .
