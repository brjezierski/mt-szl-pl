Theodor Kaluza
Theodor Franz Eduard Kaluza ( ur .
9 listopada 1885 w Opolu , zm .
19 stycznia 1954 w Getyndze ) – niemiecki matematyk i fizyk .
Znany z prób unifikacji ogólnej teorii względności z elektromagnetyzmem przy pomocy aparatu przestrzeni pięciowymiarowej .
Życiorys .
Syn Maxa Kaluzy ( 1856–1921 ) , profesora anglistyki w Królewcu , i Amalii Zaruby ( 1850–1924 ) .
Theodor , zgodnie z zapisami w księgach metrykalnych w opolskim kościele katedralnym ( Podwyższenia Krzyża św .
) , w którym został ochrzczony , urodził się w Opolu .
Mieszkał w domu przy ulicy Fesselstrasse 9 ( obecnie Strzelców Bytomskich 9 ) na Wyspie Pasiece .
W wieku 3 lat wyjechał z rodzicami do Królewca , gdzie ukończył studia i obronił doktorat z fizyki na podstawie dysertacji pt .
`` Die Tschirnhaustransformation algebraischer Gleichungen mit einer Unbekannten '' ( `` Transformacja Tschirnhausa Równań Algebraicznych z Jedną Niewiadomą '' , Uniwersytet w Królewcu , 1907 ) .
Prowadził badania nad grawitacją i falami elektromagnetycznymi w pięciowymiarowej przestrzeni .
Kaluza jest znany za sprawą sformułowania teorii Kaluzy-Kleina , łączącej teorię względności z elektromagnetyzmem za pomocą pięciowymiarowej przestrzeni , nad czym pracował m.in .
w 1919 .
Jego koncepcja interpretacji fundamentalnych sił fizycznych z użyciem dodatkowych wymiarów stała się popularna znacznie później , z nadejściem teorii strun .
Był poliglotą , znał 16 języków obcych .
