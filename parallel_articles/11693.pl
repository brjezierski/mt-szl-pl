Theresa May
Theresa Mary May z domu Brasier ( IPA : // ; ur .
1 października 1956 w Eastbourne ) – brytyjska polityk , lider Partii Konserwatywnej .
Od 13 lipca 2016 do 24 lipca 2019 premier Zjednoczonego Królestwa Wielkiej Brytanii i Irlandii Północnej .
Życiorys .
Młodość i początki kariery .
Jest absolwentką Uniwersytetu Oksfordzkiego , gdzie w 1977 uzyskała licencjat z geografii .
Po studiach została pracownicą Banku Anglii , z którym była związana do 1983 .
Następnie została konsultantką ds .
finansów w UK Payments Administration , brytyjskiej odpowiedniczce polskiej Krajowej Izby Rozliczeniowej .
Równolegle z karierą zawodową stawiała pierwsze kroki w polityce .
W 1986 została wybrana do rady londyńskiej dzielnicy Merton , gdzie w latach 1988–1990 stała na czele komisji edukacji , a od 1992 do 1994 była wiceprzewodniczącą frakcji Partii Konserwatywnej .
