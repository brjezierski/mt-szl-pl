Thetford Mines
Thetford Mines – miasto w Kanadzie , w prowincji Quebec , w regionie Chaudière-Appalaches i MRC Les Appalaches .
Miasto położone jest nad rzeką Bécancour .
Thetford Mines zostało założone jako `` Kingsville '' w 1876 roku przez Josepha Fecteau , po odkryciu w pobliżu złóż chryzotylu .
Dzisiejszą nazwę nadano w 1905 roku .
Liczba mieszkańców Thetford Mines wynosi 25 704 .
Język francuski jest językiem ojczystym dla 96,5 % , angielski dla 1,5 % mieszkańców ( 2006 ) .
