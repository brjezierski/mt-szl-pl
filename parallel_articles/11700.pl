Thionne
Thionne – miejscowość i gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Allier .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 337 osób , a gęstość zaludnienia wynosiła 13 osób/km² .
W styczniu 2015 r. Thionne zamieszkiwało 335 osób , przy gęstości zaludnienia wynoszącej 12,9 osób/km² .
