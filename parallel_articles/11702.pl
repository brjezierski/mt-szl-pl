Thiruvananthapuram
Thiruvananthapuram ( hindi तिरुवनंतपुरम , trb .
: `` Tiruwanantapuram '' , trl .
: `` Tiruvanaṁtapuram '' ; malajalam : തിരുവനന്തപുരം , trl .
: `` Tiruvanantapuram '' ; ang .
`` Thiruvananthapuram '' ; do 1996 hindi त्रिवेन्द्रम , trb .
: `` Triwendram '' , trl .
: `` Trivendram '' ; ang .
`` Trivandrum '' ) – stolica stanu Kerala .
Miasto położone jest po zachodniej stronie wybrzeża , prawie na samym krańcu Półwyspu Indyjskiego .
Z liczbą ludności wynoszącą 745 tys .
Thiruvananthapuram staje się największym ośrodkiem Kerali .
Miejska aglomeracja liczy sobie około miliona ludności .
W mieście znajduje się wiele siedzib rządu , organizacji i firm .
Oprócz ośrodka politycznego , jest to miasto , w którym dużą wagę przywiązuje się do edukacji - jest tu kilka instytucji edukacyjnych , takie jak Uniwersytet Kerala oraz szkoły informatyczne jak Vikram Sarabhai Space Centre .
