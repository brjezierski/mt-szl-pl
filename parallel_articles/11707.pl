Thorigny-sur-le-Mignon
Thorigny-sur-le-Mignon – miejscowość i dawna gmina we Francji , w regionie Nowa Akwitania , w departamencie Deux-Sèvres .
W 2016 roku populacja ówczesnej gminy wynosiła 107 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia trzech ówczesnych gmin – Priaires , Thorigny-sur-le-Mignon oraz Usseau – powstała nowa gmina Val-du-Mignon .
Siedzibą gminy została miejscowość Usseau .
