Thurso
Thurso ( gael .
`` Inbhir Theòrsa '' ) − miasto na północnym wybrzeżu Szkocji w regionie Caithness , założone prawdopodobnie przez wikingów ok. 1000 roku n.e .
Liczba mieszkańców w 2003 roku wynosiła ok. 8 tys .
Odpływają stąd promy na pobliskie Orkady .
W pobliżu miasta znajduje się ośrodek badań jądrowych i nieczynna elektrownia jądrowa Dounreay Nuclear Power Development Establishment .
W mieście znajduje się stacja kolejowa - położony najdalej na północy fragment sieci kolejowej Wielkiej Brytanii .
