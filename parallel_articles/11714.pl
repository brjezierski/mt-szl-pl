Thérouanne ( stolica tytularna )
Thérouanne ( łac .
`` Dioecesis Morinensis '' ) – stolica historycznej diecezji we Francji erygowanej ok. VI wieku , a włączonej w 1567 w skład diecezji Boulogne .
Współcześnie miasto Thérouanne znajduje się w regionie Hauts-de-France we Francji .
Obecnie katolickie biskupstwo tytularne ustanowione w 2009 przez papieża Benedykta XVI .
