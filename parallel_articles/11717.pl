Tic Tac
Tic Tac ( oficjalnie tic tac ) – małe drażetki o różnych smakach produkowane przez firmę Ferrero , od 1969 roku .
Są sprzedawane w małych plastikowych opakowaniach .
Wcześniej były barwione na różne kolory odpowiadającym im smakom , obecnie drażetki Tic Tac są białe , a przezroczyste pudełka są kolorowe w zależności od smaku .
Wyjątkami są niektóre edycje limitowane .
