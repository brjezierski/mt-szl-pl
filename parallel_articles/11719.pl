Tielt
Tielt – miasto i gmina w północno-zachodniej Belgii ( prowincja Flandria Zachodnia ) .
Liczy ok. 19 tys .
mieszkańców ( 2008 ) .
Znajduje się tu cmentarz komunalny , na którym pochowani są żołnierze polegli w czasie I i II wojny światowej .
Nekropolia liczy 123 groby .
Są tutaj pochowani żołnierze belgijscy , francuscy oraz polscy .
Zostało tu pochowanych ośmiu żołnierzy 1 Dywizji Pancernej gen. Stanisława Maczka .
Zginęli oni w walkach o miasto we wrześniu 1944 roku .
Polskie groby oznaczone są biało-czerwonym godłem .
