Tiencin
Tiencin ( ; ) – jedno z czterech miast wydzielonych Chin , znajdujące się pomiędzy Pekinem a zatoką Bohai , jeden z największych portów świata i specjalna strefa ekonomiczna ustanowiona po trzęsieniu ziemi z 1976 r .
Historia .
Miasto przez długi czas było tylko małą wioską .
Jego rozwój , jako ośrodka handlowego , zaczął się po ukończeniu Wielkiego Kanału , za czasów panowania dynastii Sui .
Do 1404 roku miasto nosiło nazwę `` Zhigu '' ( 直沽 ) , co oznacza `` Otwarty Port '' .
W późniejszym czasie nazwa miasta została przemianowana na `` Tiencin '' ( Tianjin ) .
Niedługo miasto zostało ufortyfikowane fortem `` Tianjin Wei '' ( 天津卫 ) .
Tiencin stał się osobną prefekturą już w 1725 roku .
W 1731 roku jego prawa zostały jeszcze bardziej rozszerzone .
W 1856 roku do portu przybył chiński statek z towarami będącymi własnością floty Hongkongu .
Pojmano wtedy 12 mężczyzn i postawiono zarzut piractwa .
