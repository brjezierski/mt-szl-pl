Bendery ( rejon )
Bendery ( ros .
`` Бендеры '' , rum .
`` Bender '' / '' Tighina '' ) – jeden z 7 rejonów Naddniestrza ( miastem na prawach rejonu ) , znajdujący się w południowej części kraju .
Stolicą rejonu są Bendery .
Rejon poza samym miastem Bendery obejmuje również miejscowości Proteagailovca i Gîsca , które po wojnie secesyjnej Naddniestrza z 1992 pozostały pod jego kontrolą , choć Mołdawia uznaje je za swoją własność .
Z kolei Naddniestrze uznaje za część rejonu Bendery również miejscowość Varniţa pozostającą pod administracją mołdawską ( obszar zakreskowany na mapie ) .
