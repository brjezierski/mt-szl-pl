Tigre ( grupa etniczna )
Tigre – grupa etniczna zamieszkująca Erytreę oraz południową część sudańskiej Prowincji Morza Czerwonego .
Istnieją także duże diaspory Tigra w Norwegii , Wielkiej Brytanii i Kanadzie .
Ich populację szacuje się na ponad 1,1 miliona i stanowią jedną piątą populacji Erytrei .
Są blisko spokrewnieni z Tigrajczykami i Bedża .
Posługują się semickim językiem tigre .
