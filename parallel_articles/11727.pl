`` Takrit '' / `` Tikrit '' ) – miasto ( 151 tys .
mieszkańców ) nad Tygrysem w północnym Iraku ; stolica prowincji Salah ad-Din .
W późnej starożytności Tikrit był siedzibą mafriana , czyli przywódcy monofizyckiego kościoła jakobickiego w przynależącej do imperium Sasanidów Mezopotamii ; ten fragment Międzyrzecza był bowiem jakobicki , podczas gdy pozostała część podlegała Kościołowi Wschodu i wyznawała chrześcijaństwo w wersji nestoriańskiej .
Mafrian rezydował w Tikricie do 1156 roku , kiedy przeniósł się do Mosulu , gdzie mafrianat zanikł po 1860 r. Istniały także inne , lecz mniej znane i ważne , mafrianaty .
W średniowieczu teren geograficzny Górnej Mezopotamii , na którym leży miasto , nazywano Dżezira .
Tikrit jest znany głównie jako miejsce narodzin Saladyna .
W pobliskiej wiosce Al-Audża urodził się również Saddam Husajn .
Większość elity rządzącej za panowania Saddama Husajna wywodziła się z tego miasta .
Miasto w większości zamieszkane jest przez sunnitów , którzy są w Iraku mniejszością .
W czerwcu 2014 miasto zajęli sunniccy ekstremiści z ugrupowania Państwo Islamskie .
