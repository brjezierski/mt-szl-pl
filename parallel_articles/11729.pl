Tilletia indica
Tilletia indica Mitra – gatunek grzybów z klasy płaskoszy ( `` Exobasidiomycetes '' ) .
Grzyb mikroskopijny , obligatoryjny pasożyt roślin , u pszenicy i pszenżyta wywołujący chorobę o nazwie śnieć indyjska pszenicy .
Systematyka i nazewnictwo .
Gatunek ten po raz pierwszy opisał w 1831 r. Manoranjan Mitra w Indiach Synonim : `` Neovossia indica '' ( Mitra ) Mundk .
1941 .
Morfologia i rozwój .
Endobiont , którego strzępki rozwijają się między komórkami zaatakowanych roślin .
W zalążniach porażonych kwiatów zbóż powstają kupki teliospor całkowicie ukryte przez plewy .
Zwykle w kłosie zainfekowane są tylko niektóre ziarniaki i zwykle zmiany chorobowe ograniczają się do obszaru szwu , niszcząc tkankę zarodka i przylegające bielmo .
Masa zarodników sproszkowana , brązowo-czarna , złożona z zarodników wymieszanych ze sterylnymi komórkami , w stanie świeżym wydzielająca nieprzyjemny zapach śledzi .
Jałowe komórki kuliste do wydłużonych , często o kształcie łezki , żółtawe do żółtawo-brązowych , o gładkiej , grubej ( 3-8 µm grubości ) , laminowanej ściance , zazwyczaj z trzonkiem .
