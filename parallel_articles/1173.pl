Góra Świętej Anny ( województwo dolnośląskie )
Góra Świętej Anny ( dawniej Anabrzeg ( Annabrzeg ) , niem .
`` Annaberg '' , kolonia `` Wühleisen '' ) – osada w sołectwie Kromolin położona w Polsce w województwie dolnośląskim , w powiecie głogowskim , w gminie Żukowice .
W latach 1975–1998 miejscowość administracyjnie należała do województwa legnickiego .
Kaplica św .
Anny .
W miejscowości znajduje się kaplica pw .
św .
Anny wzmiankowana w dokumentach już w 1514 roku .
Pierwotnie był to budynek drewniany .
Obecny murowany powstał w 1716 roku z inicjatywy ówczesnego właściciela Kromolina – hrabiego Johanna Christopha von Churschwandt .
Kolejne przebudowy nastąpiły w latach 1740 , 1796 , 1881 i 1882 o czym świadczą daty na tylnej ścianie ołtarza .
Wnętrze utrzymane jest w stylu barokowym .
Przed kaplicą stoi figura św .
Jana Nepomucena .
Kaplica położona jest na zalesionym wzgórzu , na które prowadzi stroma ścieżka i schody .
Z tego powodu została zniszczona w czasie działań wojennych w 1945 roku jako doskonały punkt obserwacyjny i orientacyjny .
Po wojnie została odbudowana .
Obecnie raz w roku w niedzielę , po święcie jej patronki ( 26 lipca ) odbywa się odpust i poświęcenie samochodów .
Rezerwat .
W pobliżu miejscowości znajduje się Rezerwat przyrody Annabrzeskie Wąwozy o powierzchni 56,11 ha .
