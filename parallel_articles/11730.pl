Tim Conway
Tim Conway , właśc .
Thomas Daniel Conway ( ur .
15 grudnia 1933 w Willoughby , zm .
14 maja 2019 w Los Angeles ) – amerykański aktor filmowy i telewizyjny .
Życiorys .
Urodził się w Willoughby w Ohio .
Wychowywał się w Chagrin Falls .
Uczęszczał na Bowling Green State University w Bowling Green .
W latach 1956–1958 służył w United States Army .
Następnie podjął pracę jako DJ w stacji radiowej w Cincinnati .
Chociaż urodził się jako Thomas , zmienił imię na Tim , ponieważ w tym czasie był już popularny aktor Tom Conway .
