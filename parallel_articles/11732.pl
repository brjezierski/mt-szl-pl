Historia .
Pierwsze wzmianki o Timișoarze pochodzą z 1212 roku .
W 1316 r. w mieście rezydował król węgierski Karol I Robert , następnie zamek przeszedł w ręce węgierskiego regenta i wojewody siedmiogrodzkiego Jana Hunyadyego .
