Timo Werner
Timo Werner ( ur .
6 marca 1996 w Stuttgarcie ) – niemiecki piłkarz , występujący na pozycji napastnika w niemieckim klubie RB Lipsk oraz w reprezentacji Niemiec .
Syn Günthera Schuha , byłego piłkarza i trenera .
Kariera klubowa .
Werner jest wychowankiem VfB Stuttgart .
1 sierpnia 2013 zadebiutował w barwach VfB Stuttgart w meczu fazy kwalifikacyjnej Ligi Europy z Botewem Płowdiw .
Miał wtedy 17 lat , cztery miesiące i 25 dni , co czyniło go najmłodszym debiutantem w historii klubu .
17 sierpnia 2013 zadebiutował w Bundeslidze , w spotkaniu z Bayerem 04 Leverkusen .
22 września 2013 strzelił swoją pierwszą bramkę dla VfB Stuttgart , przeciwko Eintrachtowi Frankfurt .
10 listopada trafił dwa gole w meczu z SC Freiburg i stał się najmłodszym piłkarzem w historii Bundesligi , który zdobył dwa gole w jednym spotkaniu .
W czerwcu 2016 podpisał czteroletni kontrakt z RB Leipzig .
18 czerwca 2020 Chelsea potwierdziła transfer Wernera i podpisała z nim pięcioletnią umowę .
Kariera reprezentacyjna .
W reprezentacji Niemiec zadebiutował 22 marca 2017 w meczu z Anglią .
