Tina Turner
Tina Turner , właśc .
Anna Mae Bullock ( ur .
26 listopada 1939 w Nutbush ) – piosenkarka soulowa i rockowa , autorka tekstów , choreografka , tancerka , muzyk , aktorka , producentka i pisarka urodzona w Stanach Zjednoczonych oraz posiadająca obywatelstwo Szwajcarii .
Należy do grupy artystów , którzy zdefiniowali główny nurt rocka lat 80 .
XX wieku .
Jest jedną z najbardziej dochodowych artystek pod względem sprzedaży koncertów na świecie i jednym z najpopularniejszych artystów w historii .
Szacuje się , że nakład ze sprzedaży wszystkich wydawnictw Turner wynosi ponad 180 mln egzemplarzy , co czyni ją rockową artystką z największą liczbą sprzedanych nagrań .
Niektóre źródła podają jednak , że liczba sprzedanych przez nią płyt wynosić może nawet 200 mln sztuk .
Ze względu na dokonania zawodowe nazywana jest „ królową rock and rolla ” .
Początki aktywności Turner w przemyśle muzycznym datuje się na lata 1959–1960 .
We wczesnych latach kariery występowała wraz z Ikiem Turnerem w zespole rock and rollowym Ike & amp ; Tina Turner jako wokalistka .
