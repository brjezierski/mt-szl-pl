Język tindyjski
Język tindyjski ( nazwa własna Ъидараб миц̄и , ros .
тиндинский язык ) – jeden z niewielkich języków kaukaskich , używany przez Tindów .
Należy do języków andyjskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Jest blisko spokrewniony z językiem bagulalskim .
Dane o liczbie osób posługujących się tym językiem znacząco różnią się w zależności od źródeł .
Według statystyk z 1926 r. języka tego używało 3812 osób ; później Tindowie uznawani byli za odłam Awarów i tak też zapisywani podczas kolejnych spisów powszechnych .
Szacunki z roku 1958 mówiły o ok. 5 tys .
użytkowników języka , natomiast późniejsze źródła podają rozbieżne dane – od 10 tys .
