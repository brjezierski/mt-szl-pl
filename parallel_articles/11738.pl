Atrament
Atrament ( , czernidło ) – środek barwiący w postaci roztworu barwnika w rozpuszczalniku z ewentualnym dodatkiem substancji zagęszczających ( np .
żelujących ) .
Wykorzystywany jest w szeregu przyborów pisarskich ( długopisy , flamastry , markery , wieczne pióra ) oraz w druku atramentowym ( drukarki atramentowe , plotery ) .
Atrament jest mniej lub bardziej przezroczysty ( ponieważ zawiera rozpuszczone barwniki ) , czym różni się od tuszu , stosowanego w podobnych materiałach pisarskich i urządzeniach drukujących , ale mającego właściwości kryjące – jednak atramenty do drukarek i ploterów zawierają również pigmenty ( zob .
składniki atramentów w drukarkach i ploterach ) .
Poprzednikiem atramentu , a także jego dawną nazwą , był inkaust .
