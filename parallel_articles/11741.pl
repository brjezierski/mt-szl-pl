Tirana
Tirana ( lub ) – stolica i największe miasto Albanii położone nad 3 rzekami : Lanë , Tiranë i Tërkuzë .
Jest to największy ośrodek przemysłowy i centrum kulturalne kraju .
W mieście znajduje się 7 szkół wyższych , z których najważniejszą jest założony w 1957 Uniwersytet Tirański .
Głównym zabytkiem miasta jest meczet Ethema Beja z XVIII wieku .
Znajdują się tu także Teatr Narodowy oraz Teatr Opery i Baletu .
17 km na północny zachód położony jest port lotniczy Tirana im .
Matki Teresy .
W mieście znajduje się stacja kolejowa Tirana .
