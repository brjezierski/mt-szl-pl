Tkibuli
Tkibuli ( ) – miasto w środkowej Gruzji , w regionie Imeretia .
Liczba mieszkańców w 2014 wynosiła 9 770 .
W mieście znajduje się jedna kopalnia węgla , choć wcześniej było ich dziewięć .
W rejonie Tkibuli rośnie także herbata , która sprzedawana jest w całej Gruzji .
Miasto znajduje się na linii kolejowej Kutaisi - Tkibuli .
