Tofaş
Tofaş ( `` Türk Otomobil Fabrikasi A.Ş . '' )
– turecki producent samochodów osobowych i lekkich dostawczych produkowanych od 1971 roku .
Fiat Chrysler Automobiles posiada 37,86 % akcji w spółce Tofaş , a wszystkie produkowane przez nią modele bazowały na licencjach włoskiego koncernu .
Wśród akcjonariuszy tureckich największą rolę odgrywa Koç Holding posiadający 37,59 % akcji .
Nowsze modele produkowane są pod marką FIAT .
W listopadzie 2007 roku rozpoczęła się w zakładach Tofaş produkcja modeli dostawczych z gamy Sevel .
Zdolność produkcyjna fabryki przed 2007 rokiem wynosiła 250 tys .
