Historia .
Na mocy podpisanej w 1968 roku umowy licencyjnej między Fiatem a przedsiębiorstwem Tofaş w lutym 1971 roku rozpoczęto w zakładzie w Bursie w Turcji wytwarzanie modelu Tofaş 124 Murat opartego technicznie na Fiacie 124 .
Był on produkowany do 1976 r. W latach 1983–1994 wznowiono jego produkcję pod nazwą Tofaş Serce .
W 1977 r. rozpoczęła się produkcja Fiata 131 pod nazwą Tofaş Murat 131 , od 1981 r. powstawał on jako sedan Tofaş Sahin , luksusowy sedan Tofaş Doğan oraz kombi Tofaş Kartal .
Od 2002 r. powyższe modele produkowano tylko na eksport .
W 1990 roku uruchomiono produkcję modelu Tempra , od 1993 r. Uno II , od 1994 r. Tempra Station Wagon i Tipo pod marką Tofaş z silnikami 1,6 dm³ ( 97-104 KM ) i 2,0 dm³ ( 148 KM ) .
Model Uno był wyposażany w silnik 1,4 dm³ ( 72-73 KM ) .
Prawdopodobnie w 1997 r. Fiat stał się największym udziałowcem spółki .
W 1998 r. po raz pierwszy pod marką Fiat zaczęto produkcję modeli Siena , Palio i Palio Weekend .
We wrześniu 1999 r. wprowadzono modele Brava i Marea .
W 2002 r. Siena została zastąpiona przez zmodernizowany model Albea , zmodernizowano również model Palio hatchback .
Dwa ostatnie modele ponownie zmodernizowano w 2005 roku .
W latach 1999–2002 produkowano tu Fiata Brava , a w latach 1999–2006 był produkowany sedan Marea .
Na salonie w Paryżu 2000 zadebiutował minivan Fiat Doblò , który jest produkowany głównie w Turcji ( drugi zakład to Fiat Automóveis w Betim w Brazylii ) w odmianach użytkowych i osobowych i stąd eksportowany głównie do Europy .
Stał się on najpopularniejszym modelem przedsiębiorstwa , z maksymalnym udziałem w produkcji sięgającym około 2/3 .
W 2005 r. Doblò został zmodernizowany .
W połowie 2006 r. zakończono produkcję modeli Doğan i Kartal , zaś w marcu 2008 roku modelu Sahin .
Od marca 2007 r. tylko w fabryce Tofaş powstaje Fiat Linea .
W połowie 2005 roku podpisano umowę o utworzeniu spółki pomiędzy Fiatem a PSA Peugeot Citroën , zajmującej się opracowaniem i produkcją lekkich samochodów dostawczych .
W listopadzie 2007 r. rozpoczęła się ich produkcja w zakładzie w Bursie w Turcji .
FIAT Auto i PSA Peugeot Citroën wspólnie ze spółką Tofaş Türk Otomobil Fabrikası A.Ş .
uruchomiły produkcję lekkich samochodów dostawczych Fiat Fiorino II oraz Citroën Nemo i Peugeot Bipper .
