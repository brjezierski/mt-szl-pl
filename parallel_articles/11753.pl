Togo ( pasmo górskie )
Togo ( fr .
`` Chaîne du Togo '' ) – pasmo górskie rozciągające się poprzez centralny region Togo w Afryce Zachodniej oraz wzdłuż jego wschodniej i zachodniej granicy aż do wschodniej Ghany i północnego Beninu .
W Ghanie znane są jako wzgórza Akwapim ( ang .
`` Akwapim Hills '' ) , a w Beninie jako góry Atakora ( fr .
`` Chaîne de l'Atakora '' ) .
Najwyższym szczytem pasma jest Mont Agou ( 986 m n.p.m .
) , będący jednocześnie najwyższym szczytem w Togo .
Do pasma należą także najwyższe góry Beninu – Sokbaro ( 658 m n.p.m . )
i Tanekas ( 641 m n.p.m . ) .
W północnej części gór na terytorium Beninu ma swoje źródła rzeka Oti .
Pasmo złożone jest głównie z piaskowca , łupków i kwarcytu .
