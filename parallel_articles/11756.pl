Toki pona
Toki pona ( dobry/prosty język ) – sztuczny język stworzony przez kanadyjską tłumaczkę i lingwistkę Sonję Lang .
Pierwszy raz zaprezentowany został w połowie 2001 roku .
Toki pona składa się z 14 głosek i oryginalnie 120 wyrazów .
Jego powstanie było zainspirowane m.in .
taoizmem , minimalizmem , prymitywizmem i redukcjonizmem .
Język ten miał wpłynąć na sposób myślenia swoich użytkowników , został opracowany dla zademonstrowania prawdziwości hipotezy Sapira-Whorfa .
Według nowego spisu Toki Pony , 165 i więcej osób mówi płynnie w toki ponie , a około 550 osób uczy się go z różnym skutkiem .
W 2014 roku Sonja Lang wydała na temat języka książkę `` Toki Pona : The Language of Good '' .
Przykładowy tekst ( Ojcze Nasz )
Etymologia .
Nazwa języka składa się ze słowa `` toki '' ( język ) , pochodzący od wyrazu `` tok '' z języka tok pisin , które z kolei wywodzi się od angielskiego `` talk '' ; natomiast `` pona '' ( dobry/prosty ) , z esperanto `` bona '' ( dobry ) , ostatecznie od łacińskiego `` bonus '' .
Cel .
Sonja Lang rozwinęła toki pona jako sposób uproszczenia jej myślenia podczas depresji , mając na uwadze kilka celów .
Jednym z głównych celów języka jest skupienie się na minimalizmie .
Został zaprojektowany aby wyrazić jak najwięcej w jak najmniej skomplikowany sposób .
Ma 120–125 słów kluczowych oraz 14 głosek opracowanych w sposób łatwy do wymówienia dla osób posługujących się różnorodnymi językami .
Kolejną ideą toki pona jest zwrócenie uwagi jej użytkowników na chwilę obecną i otoczenie oraz na słowa , których używamy .
Według jego autorki , miał być `` zabawny i uroczy '' .
Mimo iż nie został stworzony z myślą o byciu międzynarodowym językiem pomocniczym , może być w tym celu używany , gdyż ludzie z całego świata używają go do wspólnej komunikacji .
Pochodzenie słów w języku .
Większość słów w języku pochodzi z angielskiego , tok pisin , fińskiego , gruzińskiego , niderlandzkiego , akadiańskiego francuskiego , chorwackiego , esperanto , mandaryńskiego i kantońskiego .
