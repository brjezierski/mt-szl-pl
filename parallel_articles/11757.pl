Toki ( rejon postawski )
Toki – dawny zaścianek .
Tereny na których leżał , znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie postawskim , w sielsowiecie Duniłowicze .
Historia .
W czasach zaborów w granicach Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym folwark a następnie zaścianek leżał w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Duniłowicze .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 3 osoby , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 9 domach zamieszkiwało 43 osoby .
Miejscowość należała do parafii rzymskokatolickiej w Duniłowiczach .
Podlegała pod Sąd Grodzki w Duniłowiczach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Duniłowiczach .
