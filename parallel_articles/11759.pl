Tokushima
– miasto w Japonii , w prefekturze Tokushima , na wyspie Sikoku ( `` Shikoku '' ) , stolica prefektury Tokushima .
Miasto Tokushima powstało , kiedy daimyō Iemasa Hachisuka ( 1558–1639 ) wkroczył do regionu Awa , zbudował zamek i rozwinął przyszłe miasto zamkowe jako polityczne i gospodarcze centrum .
Rodzina Hachisuka rządziła przez 14 pokoleń .
Obecny kształt uzyskało w 1889 roku , poprzez dołączenie kilkunastu okolicznych małych miasteczek i wiosek , w ramach tworzenia nowego systemu administracyjnego .
Miasto jest obsługiwane przez Tokushima Awa-odori Airport .
Awa-odori .
`` Awa-odori '' w Tokushimie to jeden z wielu najlepiej znanych festiwali tanecznych odbywających się w całej Japonii podczas sezonu `` o-bon '' w połowie sierpnia .
Awa to dawna nazwa prefektury Tokushima , `` odori '' oznacza taniec .
