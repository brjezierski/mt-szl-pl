Anchorage
Anchorage ( oficjalnie Municipality of Anchorage ) – skonsolidowane miasto-okręg ( ang .
`` consolidated city-borough '' ) w Stanach Zjednoczonych położony w stanie Alaska nad Zatoką Cooka ( Ocean Spokojny ) .
Obecny status miasto uzyskało w 1975 roku .
Miasto zamieszkuje 279 671 osób ( 2007 ) , czyli ponad 40 % wszystkich mieszkańców Alaski .
Historia .
Miasto zostało założone w 1914 .
Jego nazwa oznacza w języku angielskim „ miejsce kotwiczenia , kotwicowisko ” .
W 1920 otrzymało prawa samorządowe .
W czasie II wojny światowej stanowiło ważną bazę sił powietrznych i lotnisko .
Tankowano tutaj samoloty lecące z Ameryki i Europy na Daleki Wschód .
W 1954 powstał tu uniwersytet University of Alaska .
27 marca 1964 miasto nawiedziło bardzo silne trzęsienie ziemi o magnitudzie 9,2 stopnia w skali Richtera .
W jego wyniku zginęło 131 osób , a szkody oszacowano na 300 milionów ówczesnych dolarów .
Geografia .
Anchorage jest położone w południowo-środkowej części Alaski .
Rozpościera się na terenie przybrzeżnej niziny , w pobliżu gór Chugach .
Stanowią one barierę rozwoju budownictwa we wschodniej części miasta , którego granice sięgają jeszcze dalej na wschód , do terenów Parku Stanowego Chugach .
