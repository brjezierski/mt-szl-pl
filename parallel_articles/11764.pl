Tolochenaz
Tolochenaz – wieś i gmina w Szwajcarii , w południowej części kantonu Vaud w dystrykcie Morges .
Powierzchnia 1,58 km² .
Około 1700 mieszkańców ( 2009 r. ) .
Leży na północnym brzegu Jeziora Genewskiego , pomiędzy Morges na wschodzie a Saint-Prex na zachodzie .
Przez miejscowość przebiega linia kolejowa Genewa – Lozanna , a także autostrada A1 , łącząca te dwa miasta .
Autostrada dzieli Tolochenaz na dwie prawie równe części , z których południowo-wschodnia , położona bliżej Jeziora Genewskiego , ma charakter przemysłowy , zaś północno-zachodnia – rolniczy i mieszkalny ( zabudowa indywidualna ) .
Od początku istnienia wsi jej mieszkańcy zajmowali się głównie rolnictwem , nie imając się w zasadzie zajęć związanych z Jeziorem Genewskim ( wieś w granicach administracyjnych nie ma portu , a brzegi jeziora są w znacznej części niezagospodarowane ) .
Obecnie istnieje tu dobrze rozwinięte sadownictwo z uprawą jabłoni i grusz .
