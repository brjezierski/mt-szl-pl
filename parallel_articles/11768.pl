Tomasz Tomczykiewicz
Tomasz Kazimierz Tomczykiewicz ( ur .
2 marca 1961 w Pszczynie , zm .
28 listopada 2015 w Katowicach ) – polski polityk i samorządowiec , w latach 1998–2001 burmistrz Pszczyny , poseł na Sejm IV , V , VI , VII i VIII kadencji , w latach 2010–2011 przewodniczący Klubu Parlamentarnego Platformy Obywatelskiej , w latach 2011–2015 sekretarz stanu w Ministerstwie Gospodarki .
Życiorys .
Syn Tadeusza i Teresy .
Z wykształcenia magister inżynier budownictwa , w 1986 ukończył studia w zakresie budownictwa wodnego na Wydziale Inżynierii Sanitarnej i Wodnej Politechniki Krakowskiej .
W 1993 ukończył studia podyplomowe w zakresie zarządzania w Szkole Głównej Handlowej w Warszawie .
W latach 1990–1998 był radnym rady miejskiej w Pszczynie oraz członkiem zarządu miasta .
W 1997 bez powodzenia kandydował do Sejmu z listy Akcji Wyborczej Solidarność .
W październiku 1998 został radnym sejmiku śląskiego , a w następnym miesiącu burmistrzem Pszczyny .
Działał w Unii Polityki Realnej , Stronnictwie Polityki Realnej , Stronnictwie Konserwatywno-Ludowym .
Od 2001 działał w Platformie Obywatelskiej , w której od 24 czerwca 2006 do 8 października 2010 pełnił funkcję wiceprzewodniczącego .
Był szefem struktur partii w województwie śląskim .
Od 2001 sprawował mandat poselski .
Był posłem IV i V kadencji , a w wyborach parlamentarnych w 2007 po raz trzeci został posłem , otrzymując w okręgu bielskim 41 035 głosów .
Został wiceprzewodniczącym komisji śledczej do zbadania okoliczności śmierci Barbary Blidy .
22 lipca 2010 został wybrany na przewodniczącego klubu parlamentarnego swojego ugrupowania , funkcję tę pełnił do końca VI kadencji Sejmu .
W wyborach w 2011 kolejny raz uzyskał mandat poselski , kandydując tym razem w okręgu katowickim i otrzymując 57 323 głosy .
20 grudnia tego samego roku został powołany na stanowisko sekretarza stanu w Ministerstwie Gospodarki w rządzie Donalda Tuska .
W czerwcu 2015 premier Ewa Kopacz przyjęła jego dymisję .
W wyborach parlamentarnych w 2015 z sukcesem ubiegał się o reelekcję , uzyskując 10 814 głosów .
Zmarł 28 listopada 2015 w wyniku choroby nerek , z którą zmagał się od lat .
Był żonaty , miał dwoje dzieci .
3 grudnia 2015 został pochowany na pszczyńskim cmentarzu św .
Jadwigi Śląskiej .
