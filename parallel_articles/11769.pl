– gatunek grzybów z rodziny chropiatkowatych ( `` Thelephoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tomentella , Thelephoraceae , Thelephorales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisali go w 1815 r. Johannes Baptista von Albertini i Lewis David von Schweinitz nadając mu nazwę `` Thelephora punicea '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1888 r. Joseph Schröter .
