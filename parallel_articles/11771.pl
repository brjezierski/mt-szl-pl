Tomsk
Tomsk ( ros .
`` Томск '' ) – miasto w Rosji , położone na Nizinie Zachodniosyberyjskiej , nad rzeką Tom ( 70 km od jej ujścia do Obu ) , liczące 576,6 tys .
mieszkańców ( 2020 ) .
Miasto jest ważnym ośrodkiem naukowym , w którym istnieje 7 szkół wyższych , w tym najstarsza uczelnia Syberii , Narodowy Badawczy Tomski Uniwersytet Państwowy założony w 1878 roku , ośrodkiem przemysłu spożywczego , drzewnego , elektromaszynowego i chemicznego .
Historia .
Miasto zostało założone w 1604 roku , a w 1804 zostało stolicą guberni .
Rozwinął się jako duży ośrodek handlowy , w związku z rozwojem wydobycia złota w guberniach tomskiej i jenisejskiej .
Od XVII w. miejsce zesłania .
Pod koniec XIX wieku stracił na znaczeniu w związku z tym , że ominął go szlak Kolei Transsyberyjskiej .
W grudniu 1917 powołano tu Syberyjską Dumę Obwodową .
W latach 1918–1919 pod rządami Korpusu Czechosłowackiego , zdobyty przez bolszewików w grudniu 1919 roku .
