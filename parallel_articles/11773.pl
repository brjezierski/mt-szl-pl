Tomáš Masaryk
Tomáš Garrigue Masaryk wym .
( ur .
7 marca 1850 w Hodonínie , zm .
14 września 1937 w Lánach ) — pierwszy i najdłużej urzędujący prezydent Czechosłowacji , nazywany `` ojcem niepodległej Czechosłowacji '' , „ prezydentem oswobodzicielem ” .
Zwany też skrótowo „ TGM ” Miał w narodzie przydomek „ Tatíček ” – tatuś/ojczulek .
Profesor filozofii , socjolog , w latach 1882–1914 wykładowca filozofii na uniwersytecie w Pradze .
Założyciel i ideolog Czeskiej Partii Postępowej , która domagała się przyznania ziemiom czeskim autonomii w ramach monarchii austro-węgierskiej ; współtwórca niepodległej Czechosłowacji .
Życiorys .
Urodził się jako Thomas Masarik w rodzinie chłopskiej w zdominowanym przez wyznawców katolicyzmu mieście Hodonín na Morawach .
Jego ojciec , Jozef Masaryk był Słowakiem z Kopczan .
Ojciec był woźnicą w majątku , urodził się jako chłop pańszczyźniany .
Jego matka Terezie Masaryková ( nazwisko panieńskie Kropáčková ) pochodziła z niemieckojęzycznej rodziny czeskiej .
Pracowała jako służącą we dworze .
Wskutek tego Masaryk dorastał jako osoba dwujęzyczna .
