Tones and I
Tones and I , właśc .
Toni Watson ( ur .
13 maja 1993 lub 15 sierpnia 2000 w Mount Martha , Wiktoria ) – australijska piosenkarka i autorka tekstów .
1 marca 2019 wydała debiutancki singiel „ Johnny Run Away ” .
10 maja premierowo zaprezentowała piosenkę „ Dance Monkey ” , z którą osiągnęła ogromny sukces komercyjny , plasując się na pierwszym miejscu na oficjalnych listach przebojów w 35 krajach , w tym Australii , gdzie pozostawał na szczycie listy ARIA Charts przez kolejne 15 notowań , bijąc rekord liczby tygodni na pierwszym miejscu w historii australijskich notowań , poprzednio prowadzonym przez „ Shape of You ” Eda Sheerana z 2017 .
„ Dance Monkey ” spędził 21 tygodni na najwyższej pozycji listy .
30 sierpnia wydała debiutancki minialbum pt .
`` The Kids Are Coming '' .
