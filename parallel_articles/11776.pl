Toni Sailer
Anton Engelbert „ Toni ” Sailer ( ur .
17 listopada 1935 w Kitzbühel , zm .
24 sierpnia 2009 w Innsbrucku ) – austriacki narciarz alpejski , aktor i piosenkarz , trzykrotny mistrz olimpijski i wielokrotny złoty medalista mistrzostw świata .
Kariera .
Pierwsze sukcesy w karierze Toni Sailer osiągnął w 1956 roku , kiedy podczas igrzysk olimpijskich w Cortina d ’ Ampezzo zdobył trzy złote medale .
Austriak został pierwszym alpejczykiem , który wygrał wszystkie trzy konkurencje olimpijskie .
Największą przewagę osiągając w slalomie gigancie , w którym wyprzedził drugiego na mecie Andreasa Molterera o 6,2 sekundy .
Igrzyska w Cortina d ’ Ampezzo były równocześnie mistrzostwami świata , jednak kombinację rozgrywano tylko w ramach drugiej z tych imprez .
Sailer zwyciężył również w tej konkurencji , wyprzedzając Francuza Charlesa Bozona o ponad 12 punktów oraz Szweda Stiga Sollandera o blisko 17 punktów .
Został tym samym pierwszym alpejczykiem w historii , który zdobył trzy złote medale na jednych igrzyskach .
Wynik ten wyrównał 12 lat później Francuz Jean-Claude Killy .
Do 2010 roku pozostawał także jedynym Austriakiem , który zdobył trzy złote medale olimpijskie ( na ZIO 2010 swoje trzecie tytuły olimpijskie zdobyli dwuboista Felix Gottwald oraz skoczek narciarski Thomas Morgenstern ) .
Na rozgrywanych dwa lata później mistrzostwach świata w Bad Gastein zdobył kolejne cztery medale .
Sailer zwyciężył tam w zjeździe , gigancie i kombinacji , a w slalomie zajął drugie miejsce .
W tych ostatnich zawodach rozdzielił na podium swego rodaka Josefa Riedera oraz Chiharu Igayę z Japonii .
Strata Sailera do Riedera wyniosła zaledwie 0,7 sekundy .
Ponadto w 1957 roku został potrójnym mistrzem Austrii , wygrywając slalom , slalom gigant i kombinację , w latach 1956–1957 wygrywał zawody Hahnenkammrennen w Kitzbühel , a w latach 1955-1958 cztery razy z rzędu był najlepszy w zawodach Lauberhornrennen w Wengen .
W latach 1956 , 1957 i 1958 otrzymywał tytuł austriackiego sportowca roku .
W 1958 roku zakończył karierę .
W latach 1972-1976 był głównym trenerem reprezentacji Austrii w narciarstwie alpejskim oraz dyrektorem technicznym Austriackiego Związku Narciarskiego i delegatem technicznym międzynarodowego związku narciarskiego FIS .
W 1985 roku otrzymał Order Olimpijski .
Po zakończeniu kariery prowadził hotel .
Występował także w filmach ( ukończył Berlińską Szkołę Aktorską ) , nagrywał piosenki ( 25 nagrań płytowych ) oraz pisał książki .
W 2000 roku został wybrany austriackim sportowcem stulecia .
Zmarł 24 sierpnia 2009 roku na nowotwór złośliwy krtani .
Jego siostra Rosi Sailer oraz brat Rudi Sailer również uprawiali narciarstwo alpejskie .
