Garnitur
Garnitur – męski ubiór jedno- , dwu- lub trzyczęściowy , składający się ze spodni , marynarki i kamizelki ( w niektórych garniturach kamizelka bywa pomijana ) .
Zazwyczaj wszystkie trzy elementy uszyte są z tego samego materiału ( tkaniny ) , czasami stosuje się również kamizelkę w innym kolorze ( np .
w ciekawy wzór – kraty , prążki , etc. , lub gładkie ) i wykończone w podobny sposób .
Dopełnieniem garnituru zwykle są odpowiednio dobrane : koszula , krawat , skarpetki i półbuty na cienkiej skórzanej podeszwie .
Inicjatorem garnituru w modzie męskiej był w XVIII w. Brytyjczyk George Bryan „ Beau ” Brummell .
