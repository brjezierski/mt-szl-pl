Toporów ( obwód lwowski )
Toporów ( ukr .
Топорів , `` Toporiw '' ) – wieś ( dawniej miasto ) na zachodniej Ukrainie w rejonie buskim obwodu lwowskiego .
W II Rzeczypospolitej miejscowość była siedzibą gminy wiejskiej Toporów w powiecie radziechowskim województwa tarnopolskiego .
Historia .
Miasto zostało założone przez Jana z Tęczyna w roku 1450 .
Do 1772 roku w województwie ruskim w I Rzeczypospolitej , a następnie do 1914 podlegało pod jurysdykcję starostwa powiatowego w Brodach w prowincji Galicja .
W 1904 miasteczko strawił pożar .
W 1914 miasteczko liczyło 5000 mieszkańców ( 1000 Polaków , 2000 Żydów i 2000 pozostałych narodowości ) .
