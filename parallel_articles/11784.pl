Topórek
Topórek – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Położona 3 km od centrum Siedlec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Przez Topórek przebiega droga powiatowa Żabokliki - Jagodna .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Maksymiliana Marii Kolbego w Siedlcach .
