Torrevieja
Torrevieja ( walenc .
`` Torrevella '' ) – miasto w południowo-wschodniej Hiszpanii , w regionie Walencja , w prowincji Alicante .
Położone na wybrzeżu Costa Blanca , około 40 kilometrów na południe od miasta Alicante i 40 km na wschód od Murcji , a populacja wynosi 91731 .
Torrevieja była wsią rybacką położoną nad morzem i dwiema dużymi kopalniami soli , dzięki którym zyskała zdrowy mikroklimat .
Pierwotnie była to mała rybacka wioska , uzależniona od „ białego złota ” , którym była wydobywana tu sól .
Morze i sól przez wiele lat stanowiły o bycie tutejszej ludności .
Obecnie słone jeziora , które były źródłem białego złota stanowią parki narodowe i zachwycają różnorodnością zamieszkującego je ptactwa .
Rozkwit miejscowości przypada na XIX wiek , gdy król Karol IV przeniósł do Torrevieja administrację kopalni soli , a następnie zadbał o rozwój infrastruktury i wymiaru sprawiedliwości .
