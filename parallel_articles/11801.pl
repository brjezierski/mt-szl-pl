Torsby
Torsby – miejscowość w środkowej Szwecji , w gminie Torsby , położone nad północnym krańcem jeziora Fryken .
W 2017 roku miasto liczyło 4477 mieszkańców .
Popularne sporty zimowe , wędkarstwo i turystyka .
Transport .
Na północy miasta znajduje się port lotniczy Torsby .
Przez miejscowość przebiega także niezelektryfikowana linia kolejowa Fryksdalsbanan , łącząca Torsby z Kil .
W Torsby krzyżują się europejskie drogi E16 i E45 .
