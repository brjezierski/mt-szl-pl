Tortellini
Tortellini – tradycyjne włoskie pierożki z nadzieniem mięsnym , grzybowym bądź serowym , podawane z sosem pomidorowym ( można również korzystać z innych sosów , lecz nie jest to wówczas w całości włoska potrawa ) .
Na koniec całość posypuje się tartym serem ( najczęściej parmezanem ) .
Podobnym rodzajem są cappelletti , które różnią się sposobem wywinięcia końcówek ciasta .
