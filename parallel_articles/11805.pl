Tortola
Tortola – największa i najbardziej zaludniona wyspa Brytyjskich Wysp Dziewiczych .
Tortola jest wyspą wulkaniczną , a jej największym wzniesieniem jest Mount Sage o wysokości 533,4 m n.p.m .
Wyspę zamieszkuje 23 908 mieszkańców .
Na Tortoli znajduje się stolica Brytyjskich Wysp Dziewiczych - Road Town .
Ludność wyspy zajmuje się rybołówstwem .
Na wyspie rozwinęła się turystyka .
