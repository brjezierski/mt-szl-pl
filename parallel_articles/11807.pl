Tory Belleci
Salvatore `` Tory '' Belleci ( ur .
30 października 1970 w Monterey ) – amerykański producent filmowy i rekwizytor .
Najbardziej znany jako prezenter programu `` Pogromcy mitów '' .
Wykonywał też rozmaite rekwizyty do takich filmów jak : ' i ' z cyklu `` Gwiezdne wojny '' , a także `` Matrix '' , `` Van Helsing '' i inne .
Początki profesjonalnej kariery .
Belleci powiedział niegdyś , że zainteresował się tworzeniem efektów specjalnych po tym , jak stworzył reportaż o filmowych efektach specjalnych , kiedy był w liceum .
W 1994 , po ukończeniu studiów w szkole filmowej San Francisco State University , zaczął pracę z Jamiem Hynemanem w małej firmie produkcyjnej .
Tory pracował jako inspicjent , załatwiając wszystko i sprzątając sklep , ale szybko awansował na wyższą pozycję .
Parę lat później zaczął pracę w Industrial Light and Magic ( ILM ) .
Pracował dla ILM przez osiem lat jako budowniczy modeli , rzeźbiarz i malarz .
W 2003 Belleci rozpoczął pracę w serii Pogromcy Mitów stworzonej przez Discovery Channel , pracując przy produkcji , za kulisami .
W drugiej serii ukazany został jako część ekipy budującej , zaś w trzeciej serii programu zaczął pracę na ekranie .
W 2005 Belleci namówił kolegę , weterana IMH , Granta Imaharę , by dołączył do ekipy programu i zajął miejsce początkowego członka , Scottie Chapman .
Wczesne , amatorskie eksperymenty naukowe .
