Toruń
( , , `` Torunium '' ) – miasto na prawach powiatu w północnej Polsce , siedziba władz samorządowych województwa kujawsko-pomorskiego – marszałka , zarządu i sejmiku województwa oraz jednostek im podporządkowanych , a także jednej z najlepszych uczelni w Polsce – Uniwersytetu Mikołaja Kopernika .
Jest również siedzibą władz powiatu toruńskiego i stolicą rzymskokatolickiej diecezji toruńskiej , jednym z miast centralnych Bydgosko-Toruńskiego Obszaru Funkcjonalnego oraz centralnym ośrodkiem Miejskiego Obszaru Funkcjonalnego .
Drugie co do wielkości miasto w województwie kujawsko-pomorskim .
Położone jest nad Wisłą i Drwęcą .
Jego prawobrzeżna część leży na Pomorzu , zaś lewobrzeżna znajduje się na Kujawach .
Toruń , z liczbą 198 273 stałych mieszkańców , zajmuje 16. miejsce pod względem liczby ludności w Polsce ( dane ze spisu powszechnego 2021r . ) .
Jest dużym ośrodkiem gospodarczym i handlowym oraz ważnym węzłem drogowym i kolejowym .
Pod Toruniem działa Pomorska Specjalna Strefa Ekonomiczna .
Toruń to ośrodek akademicki , naukowy , kulturalny i turystyczny .
W mieście działa orkiestra symfoniczna oraz teatry , kina , liczne muzea i galerie sztuki .
W ciągu miesiąca w Toruniu odbywa się średnio sto różnego rodzaju imprez kulturalnych , poczynając od festiwali , koncertów , wystaw , spektakli teatralnych , przeglądów poetyckich i fotograficznych , a na plenerowych pokazach historycznych kończąc .
Toruń jest także siedzibą Garnizonu Toruń , orkiestry wojskowej i kilku jednostek wojskowych .
Znajduje się tu 6 konsulatów honorowych .
