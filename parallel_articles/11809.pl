Toruń jest jednym z najstarszych miast Polski ( prawa miejskie uzyskał w 1232 roku ) .
W czasach Rzeczypospolitej Obojga Narodów Toruń był jednym z najbogatszych i zarazem jednym z czterech największych miast Królestwa Polskiego , miastem królewskim i hanzeatyckim , które uzyskało w 1365 roku prawo składu .
