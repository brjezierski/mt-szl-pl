Toto Cutugno
Salvatore „ Toto ” Cutugno ( ur .
7 lipca 1943 w Fosdinovo ) – włoski piosenkarz , kompozytor muzyki pop , zwycięzca Festiwalu Piosenki Włoskiej w 1980 roku oraz 35 .
Konkursu Piosenki Eurowizji w 1990 roku .
Dzieciństwo i początki kariery .
Kilka miesięcy po narodzinach jego rodzina przeprowadziła się do La Spezii , gdzie jego ojciec musiał uzupełnić służbę wojskową jako marynarz .
Już jako dziecko wykazywał duże zainteresowanie muzyką , więc jego ojciec ( z zawodu trębacz ) zachęcał syna do ćwiczeń .
W wieku dziecięcym dołączył do miejscowego zespołu muzycznego , gdzie grał m.in .
na gitarze , perkusji , fortepianie i saksofonie .
