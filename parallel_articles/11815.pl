Tottori
– miasto w Japonii , ośrodek administracyjny prefektury Tottori , w południowej części wyspy Honsiu ( `` Honshū '' ) , nad Morzem Japońskim .
Położenie .
Tottori jest największym miastem prefektury o tej samej nazwie i leży w jej wschodniej części .
Jest najbardziej znane ze swoich piaszczystych wydm , największych w Japonii , które pokrywają ponad 30 km² wybrzeża na północ od centrum miasta .
Historia .
W 1532 roku został zbudowany zamek Tottori , który służył jako regionalne centrum władzy w okresie Muromachi ( 1333–1573 ) , a miasto Tottori stało się miastem zamkowym po tym , jak Hideyoshi Toyotomi ( wówczas nosił nazwisko Hideyoshi Hashiba ) zaatakował i zdobył zamek Tottori w 1581 roku .
Pełna historia nie jest dokładnie znana poza 200-dniowym oblężeniem właśnie w 1581 roku , które zmusiło głodującą załogę zamku i daimyō Tsuneie Kikkawę , do poddania się Hideyoshiemu .
Powodem , dla którego oblężenie trwało długo jest to , że zamek był korzystnie usytuowany .
