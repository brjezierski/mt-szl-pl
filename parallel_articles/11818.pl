Tulon
Tulon ( fr .
`` Toulon '' , prowans .
`` Tolon '' ) – miasto i gmina w południowo-wschodniej Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Var , położone nad Morzem Śródziemnym .
Znajduje się ok. 40 km na wschód od Marsylii .
Tulon jest od wielu wieków ważnym portem wojennym .
Został ufortyfikowany przez Vaubana .
27 listopada 1942 roku w porcie w Toulonie doszło do samozatopienia francuskiej floty .
Współcześnie znajduje się tutaj baza francuskiej floty śródziemnomorskiej .
