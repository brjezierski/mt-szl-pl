Tuluza ( fr .
`` Toulouse '' , oks .
`` Tolosa '' ) – miejscowość i gmina we Francji , stolica regionu Oksytania , w departamencie Górna Garonna .
Położone jest nad rzeką Garonną .
Według danych na rok 2014 gminę zamieszkiwało 466 297 osób ( zespół miejski 1 312 304 mieszkańców ) , a gęstość zaludnienia wynosiła 3941 osób/km² .
Toulouse plasuje się na czwartym miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu siódmym .
Do roku 2007 Polska posiadała tam swój konsulat honorowy .
Stanowi również ważny ośrodek akademicki .
Obok Paryża i Lyonu zaliczana jest do miast o największej liczbie studentów .
Stanowi siedzibę trzech siostrzanych uniwersytetów : Uniwersytet Tuluza I - Capitole , Uniwersytet Toulouse II – Le Mirail oraz Uniwersytet Tuluza III - Paul Sabatier .
