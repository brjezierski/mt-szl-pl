Tournai ( stacja kolejowa )
Tournai ( fra : `` Gare de Tournai '' ) – stacja kolejowa w Tournai , w prowincji Hainaut , w Belgii .
Znajduje się na linii 78 Saint-Ghislain - Tournai , 87 Bassilly - Tournai i 94 Halle - Fives .
Stacja posiadała do 1980 lokomotywownię .
Dawniej jako stacja graniczna na jej terenie znajdowały się rozbudowane urzędy celne i posterunek policji .
