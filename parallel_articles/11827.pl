Townsville
Townsville – miasto portowe w północno-wschodniej Australii , w stanie Queensland , nad Oceanem Spokojnym ; założone w 1865 roku .
Położone na terenie samorządu terytorialnego City of Townsville .
Według spisu ludności z 2016 roku miasto zamieszkuje 173 815 osób .
Jedną z największych atrakcji jest pobliska Wielka Rafa Koralowa wpisana na listę światowego dziedzictwa UNESCO .
Geografia .
Townsville położone jest nad Morzem Koralowym będącym częścią Oceanu Spokojnego ( Pacyfiku ) , około 1300 km na północ od Brisbane oraz 350 km na południe od Cairns .
Leży nad płytką Zatoką Cleveland .
8 km od centrum miasta zlokalizowana jest wyspa Magnetic .
