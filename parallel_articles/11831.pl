Trabant ( żołnierz )
Trabanci , nazywani też w I Rzeczypospolitej `` drabantami '' i `` zausznikami '' – formacja wojskowa będąca strażą przyboczną wyższych oficerów .
Pierwotnie , w XIV-XVI wieku uzbrojeni w halabardy żołnierze najemni przeznaczeni do szturmów miast i twierdz , z czasem stali się rodzajem gwardii honorowej , przekształconej w gwardyjskie oddziały liniowe .
Oprócz dworów królewskich zatrudniani też na dworach możnowładców .
W Polsce rozpowszechnili się za czasów króla Zygmunta II Augusta , w Rosji w czasach Dymitra Samozwańca I , gdzie początkowo w skład oddziałów wchodzili Polacy .
Wzorując się na Polakach , mieli ich u siebie także hetmani kozaccy .
Wymieniani są też na dworze księcia Janusza Radziwiłła .
W wielu krajach trabanci przekształcili się w gwardie .
Unikatowy rodzaj drabantów pojawił się w Szwecji za czasów Karola XII .
W Austrii drabanci istnieli od czasów cesarza Maksymiliana I , dotrwali jako gwardia honorowa aż do roku 1918 .
Formację opisuje m.in .
Henryk Sienkiewicz w `` Ogniem i mieczem ''
oraz w `` Potopie '' .
