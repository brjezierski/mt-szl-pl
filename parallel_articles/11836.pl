Trans-X
Trans-X – kanadyjski zespół muzyczny założony w latach 80 .
XX wieku , grający dance .
Jego największy przebój to „ Living On Video ” .
Liderem zespołu jest Pascal Languirand .
Nazwa zespołu pochodzi od utworu niemieckiego zespołu Kraftwerk „ Trans Europa Express ” .
Historia .
Grupa Trans-X powstała zaraz po nagraniu swojej pierwszej piosenki „ Living On Video ” .
Piosenka „ Living On Video ” inspirowana była filmem `` Tron '' .
Na utworze obok Pascala Languiranda zaśpiewała Laurie Gill .
Utwór początkowo był przebojem tylko w Kanadzie , jednak szybko trafił do dyskotek na całym świecie .
Singel „ Living On Video ” sprzedał się w nakładzie 2,5 mln egzemplarzy .
Kolejny przebój , „ Message on the Radio ” , nie zdobył takiej samej popularności co „ Living On Video ” .
W 1983 roku zespół wydał singel „ 3-D Dance ” oraz longplay `` Message on the Radio '' .
Laurie Gill odeszła z zespołu krótko po wydaniu „ 3-D Dance ” .
W 1986 roku Languirand zawiesił zespół w wyniku bardzo słabej sprzedaży albumu `` Living On Video '' .
Po kilku latach Pascal Languirand wrócił jako artysta solowy .
W 1995 roku doszło do reaktywacji Trans-X .
W 2001 roku ukazał się album `` 010101 '' .
W 2006 roku ukazał się singel „ Living On Video 2K6 ” .
