Tranzschelia pruni-spinosae ( Pers . )
Dietel – gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Wywołuje rdzę śliwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tranzschelia , Tranzscheliaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten w 1801 r. opisał Christiaan Hendrik Persoon , nadając mu nazwę `` Puccinia pruni-spinosae '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu w 1922 r. Paul Dietel .
Ma 13 synonimów .
