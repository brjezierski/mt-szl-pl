Nazwa .
Nazwa pochodzi od polskiego określenia oznaczającego - `` trawnik '' .
Niemiecki językoznawca Heinrich Adamy w swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu wymienia najstarszą nazwę miejscowości w polskiej formie `` Trawnik '' tłumacząc jej znaczenie `` Wiesenthal .
Grasplatz '' czyli po polsku `` Łąka , trawnik '' .
