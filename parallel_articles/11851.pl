Trevico ( stolica tytularna )
Trevico ( łac .
`` Dioecesis Trevicensis '' ) – stolica historycznej diecezji w Italii erygowanej ok. roku 900 , a włączonej w roku 1818 w skład diecezji Lacedonia .
Współczesne miasto Trevico w prowincji Avellino we Włoszech .
Obecnie katolickie biskupstwo tytularne ustanowione w 1968 przez papieża Pawła VI .
