TriStar Pictures
TriStar Pictures Inc. ( do 1991 roku Tri-Star ) – jednostka zależna amerykańskiej wytwórni filmowej Columbia Pictures .
Jest dalszym oddziałem Columbia TriStar Motion Picture Group , której właścicielem jest koncern Sony Pictures Entertainment .
Wytwórnia powstała w 1982 roku .
Historia .
Pojęcie TriStar powstało w 1982 roku , kiedy Columbia ( wtedy jednostka zależna Coca-Coli ) , HBO i CBS zdecydowały się dzielić stale rosnącymi kosztami produkcji filmów .
Ich pierwszym filmem był `` The Natural '' z 1984 roku , a drugim nowa wersja filmu Metro-Goldwyn-Mayer z 1960 roku `` Gdzie chłopcy są '' .
CBS wycofała się z tego przedsięwzięcia w 1984 roku , chociaż wciąż zajmowała się dystrybucją niektórych filmów TriStar do 1992 .
W kwietniu 1987 , Tri-Star wszedł do przemysłu telewizyjnego jako TriStar Television .
W grudniu 1987 roku Tri-Star zaryzykował i Columbia Pictures połączyła Columbię i Tri-Star w Columbia Pictures Entertainment Inc. , zostawiając również Columbia/Tri-Star .
Oba przedsiębiorstwa kontynuowały produkcję i zajmowały się dystrybucją filmów pod oddzielnymi szyldami .
