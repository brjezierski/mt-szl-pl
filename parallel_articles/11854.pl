Gąska karbowana
Gąska karbowana ( `` Tricholoma acerbum '' ( Bull . )
Quél . )
– gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tricholoma , Tricholomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go Jean Baptiste François Bulliard w 1792 r. nadając mu nazwę `` Agaricus acerbus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Lucien Quélet w 1872 r .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
