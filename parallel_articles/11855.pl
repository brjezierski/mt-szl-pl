Gąska zielonożółta
Gąska zielonożółta ( `` Tricholoma sejunctum '' ( Sowerby ) Quél . )
– gatunek grzybów należący do rodziny gąskowatych ( `` Tricholomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tricholomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1799 r. James Sowerby nadając mu nazwę `` Agaricus sejunctus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1872 r. Lucien Quélet , przenosząc go do rodzaju `` Tricholoma '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. , dawniej w polskim piśmiennictwie mykologicznym gatunek ten miał nazwę gąska oddzielona lub bedłka oddzielona .
Morfologia .
Średnica 3-8 cm , kształt początkowo półkolisto łukowaty podwiniętym brzegiem , później płasko łukowaty z wyprostowanym brzegiem .
Często posiada na środku wybrzuszenie .
