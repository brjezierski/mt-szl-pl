Trichophyton mentagrophytes
Trichophyton mentagrophytes ( C.P .
Robin ) Sabour .
– gatunek grzybów należący do klasy `` Eurotiomycetes '' .
Grzyb mikroskopijny , dermatofit będący jednym z głównych patogenów powodujących u ludzi grzybicę .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Trichophyton , Arthrodermataceae , Onygenales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1853 r. Charles Philippe Robin nadając mu nazwę `` Microsporum mentagrophytes '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Raymond Sabour w 1895 r .
Ma około 70 synonimów .
Są nimi wszystkie odmiany i formy .
