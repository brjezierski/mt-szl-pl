Morfologia i rozwój .
Znane jest występowania `` Trichophyton mentagrophytes '' w Ameryce Północnej , Eurazji , Afryce i Australii .
Jest to drugi co do częstości występowania dermatofit ( po `` Trichophyton rubrum '' ) .
