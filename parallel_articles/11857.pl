Trichophyton schoenleinii
Trichophyton schoenleinii ( Lebert ) Langeron & amp ; Miloch .
ex Nann .
– gatunek grzybów należący do klasy `` Eurotiomycetes '' .
Grzyb mikroskopijny , dermatofit będący jednym z patogenów powodujących u ludzi grzybice skóry .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Trichophyton , Arthrodermataceae , Onygenales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1845 r. Hermann Lebert nadając mu nazwę `` Oidium schoenleinii '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Maurice Charles Pierre Langeron , S. Milochevich i John Axel Nannfeldt w 1934 r .
Synonimy :
Występowanie .
Znane jest występowania `` Trichophyton schoenleinii '' w kilku krajach Europy , w Indiach i Chinach .
Jest to dermatofit antropofilny przenoszony przez kontakt między ludźmi .
Jest najczęstszą przyczyną grzybicy głowy i drugim co do częstości występowania patogenem powodującym grzybicę paznokci ( głównym sprawcą tej grzybicy jest `` Trichophyton rubrum '' ) .
Po II wojnie światowej ze względu na poprawę stylu życia i higieny notuje się prawie całkowity zanik wielu dermatofitów antropofilnych , w tym `` Trichophyton schoenleinii '' .
