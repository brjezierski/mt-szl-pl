Charakterystyka .
Jest jednym z dermatofitów antropogennych powodujących grzybicę owłosionej skóry głowy , głównie grzybicę strzygącą u ludzi .
Jeszcze do niedawna był endemitem afrykańskim , ale od niedawna notowany jest również w Europie .
W latach 2007–2018 w Szwajcarii zidentyfikowano go jako przyczynę grzybicy skóry u 44 pacjentów , z czego 33 w ciągu ostatnich 4 lat .
Większość z nich stanowiły kobiety ( 25 ) , a najczęstszą diagnozą była grzybica owłosionej skóry głowy ( 30 przypadków ) .
Większość pacjentów z tą grzybicą stanowiły dzieci w wieku poniżej 13 lat ( 27 przypadków ) .
Głównym źródłem zarażenia byli ludzie z terenów endemicznych , zwłaszcza z Erytrei .
Jego częstsze występowanie wydaje się być związane ze zwiększonymi przepływami migracyjnymi z Erytrei .
Ponieważ odpowiada głównie za grzybicę skóry owłosionej głowy , przy czym w większości przypadków nie ma wyraźnie określonych plam łysienia , ważne jest , aby rozważyć ją jako możliwą diagnozę w obliczu łuszczenia się skóry głowy .
Grzybica skóry owłosionej głowy wywołana przez `` T. violaceum '' jest chorobą łagodną , ale nieleczona może rozprzestrzeniać się epidemicznie , zwłaszcza wśród dzieci w szkołach i przedszkolach .
Wymagane jest wtedy badanie mykologiczne , nie tylko dla postawienia prawidłowej diagnozy i danych epidemiologicznych , ale także dla zaplanowania odpowiedniego leczenia
Izolaty `` T. violaceum '' na podłożu hodowlanym rosły powoli , tworząc woskowate , nagie , pomarszczone kolonie .
Są one charakterystyczne , głębokie , fioletowo-czerwone , a czasem mają białą , woskową obwódkę .
Z czasem niektóre kultury stały się puszyste i straciły charakterystyczną morfologię kolonii .
Morfologia kolonii i cechy mikroskopowe `` T. violaceum '' ( na przykład brak zarodnikowania ) mogą pokrywać się z cechami `` Trichophyton rubrum '' .
