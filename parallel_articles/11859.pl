Trichothecium roseum
Trichothecium roseum ( Pers . )
Link – gatunek grzybów należący do rzędu rozetkowców ( `` Hypocreales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Trichothecium , Incertae sedis , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1794 r. Christiaan Hendrik Persoon nadając mu nazwę `` Trichoderma roseum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1809 r. Johann Heinrich Friedrich Link .
Synonimy :
Morfologia .
Spotykana jest głównie anamorfa ; teleomorfa ( `` Hypomyces roseus '' ) powstaje bardzo rzadko .
Na pożywce PCA kolonie rozwijają się umiarkowanie silnie , tworząc zamszowatą , płasko rozpostartą na podłożu grzybnię .
Początkowo jest ona biała , potem różowa lub pomarańczowa .
