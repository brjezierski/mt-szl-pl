Trickster ( singel Nany Mizuki )
Trickster – osiemnasty singel japońskiej piosenkarki Nany Mizuki , wydany 1 października 2008 .
Utwory `` DISCOTHEQUE '' i `` Trinity Cross '' zostały użyte kolejno jako opening i ending anime `` Rosario + Vampire season II '' .
Singel osiągnął 2 pozycję w rankingu Oricon i pozostał na liście przez 19 tygodni , sprzedał się w nakładzie egzemplarzy .
Osiągnął status złotej płyty .
