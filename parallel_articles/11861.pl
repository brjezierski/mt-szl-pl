Trewir
Trewir ( ; ; ; ) – miasto na prawach powiatu w zachodnich Niemczech , w kraju związkowym Nadrenia-Palatynat , siedziba gminy związkowej Trier-Land , port nad Mozelą .
Miasto uniwersyteckie .
Trewir pretenduje do miana najstarszego miasta w Niemczech .
W roku 16 p.n.e .
w ramach akcji romanizacyjnej na terenie celto-germańskiego szczepu Trewerów Rzymianie założyli osadę o nazwie `` Augusta Treverorum '' .
Miasto było jednym z centrów administracyjnych i „ podstolic ” cesarstwa rzymskiego , miastem rezydencyjnym Konstancjusza I i jego syna Konstantyna Wielkiego .
Za Konstantyna Wielkiego dokonano podziału Cesarstwa Rzymskiego na cztery prefektury – Wschód ( Konstantynopol ) , Illyricum ( Sirmium ) , Italia ( Mediolan ) oraz Galia ( Trewir ) .
Od 273 roku Trewir jest siedzibą biskupów katolickich .
W średniowieczu arcybiskup trewirski był jednym z książąt Rzeszy i elektorów Świętego Cesarstwa Rzymskiego ( władającym niezależnym Arcybiskupstwem Trewiru ) .
W Trewirze ok. 330 r. zmarła św .
Helena , matka cesarza Konstantyna Wielkiego , a w roku 1818 urodził się Karl Marx .
