Jest stolicą regionu autonomicznego Friuli-Wenecja Julijska .
Według danych na 2007 rok gminę zamieszkiwało 208 614 osób , 2469,1 os./km² .
Geografia .
Triest leży u północnych wybrzeży Adriatyku , nad Zatoką Triesteńską , która stanowi najbardziej na północ wysuniętą część basenu Morza Śródziemnego .
Miasto położone jest u północno-wschodnich granic Włoch , tuż przy granicy ze Słowenią .
Zajmuje amfiteatralnie opadającą ku morzu krawędź płaskowyżu Kras zamykając północną krawędź wybrzeża Dalmacji .
Najwyższy punkt tego płaskowyżu przy granicy z miastem sięga 458 m n.p.m .
Klimat Triestu jest stosunkowo ciepły – jest to najbardziej na północ wysunięte miejsce na ziemi o klimacie śródziemnomorskim .
Średnia temperatura stycznia wynosi ok. 6 °C , a lipca 24 °C .
Lata są długie , suche i upalne , natomiast zimy łagodne i deszczowe .
Charakterystyczny dla regionu jest rodzaj wiatru katabatycznego zwanego bora , który zimą przynosi chłodne i suche powietrze znad Gór Dynarskich .
