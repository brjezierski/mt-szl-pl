Triphragmium ulmariae
Triphragmium ulmariae ( DC . )
Link – gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Pasożyt rozwijający się na wiązówce błotnej ( `` Filipendula ulmaria '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Triphragmium , Raveneliaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1808 r. Augustin Pyramus de Candolle nadając mu nazwę `` Puccinia ulmariae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Johann Heinrich Friedrich Link w 1825 r .
