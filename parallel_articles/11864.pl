Tripospermum myrti
Tripospermum myrti ( Lind ) S. Hughes – gatunek grzybów z klasy `` Dothideomycetes '' .
Jest jednym z patogenów wywołujących brudną plamistość jabłek .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tripospermum , Capnodiaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1913 Jens Wilhelm Lind , nadając mu nazwę `` Triposporium myrti '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1951 Stanley John Hughes .
Morfologia .
Grzyb mikroskopijny .
Kolonia rozwija się w postaci plam o ciemnoszarej barwie , wyglądających jak zabrudzenie sadzą .
Strzępki wegetatywne o szerokości 3,5–5 μm , silnie rozgałęzione .
Konidium typu staurospora , wielokomórkowe , przypominające wyglądem rozgwiazdę .
