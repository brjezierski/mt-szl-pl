Trnawa
Trnawa ( słow .
`` Trnava '' , węg .
`` Nagyszombat '' , niem .
`` Tyrnau '' ) – miasto w zachodniej Słowacji , stolica kraju trnawskiego , siedziba powiatu Trnawa .
Leży 45 km na wschód od Bratysławy .
Pod koniec 2019 roku , z liczbą mieszkańców przekraczającą 65 tys. , Trnawa zajmowała siódme miejsce wśród najludniejszych słowackich miast .
W mieście rozwinął się przemysł maszynowy , samochodowy , szklarski , cukierniczy , młynarski , mięsny , cukrowniczy , drzewny oraz poligraficzny .
Historia .
Po raz pierwszy wzmiankowana w 1211 roku jako Sumbot .
Leżała wówczas w granicach Węgier .
W 1238 miejscowość otrzymała od króla Węgier Beli IV prawa miasta królewskiego , jako pierwsze miasto na terenie współczesnej Słowacji .
W latach 1327 i 1360 miejsce spotkań królów Węgier oraz Czech .
W Trnawie w nocy z 10 na 11 września 1382 r. zmarł Ludwik Węgierski , król Węgier i Polski .
W 1430 stoczona została bitwa pod Trnawą pomiędzy czeskimi husytami a połączonymi siłami cesarsko-węgiersko-serbskimi .
W latach 1543–1820 ( początkowo w związku z okupacją turecką Ostrzyhomia ) Trnawa była siedzibą prymasów Węgier .
Po powstaniu Czechosłowacji została w 1922 roku utworzona administratura apostolska , którą w 1977 papież Paweł VI przekształcił w archidiecezję .
W 1635 utworzono tu uniwersytet , przeniesiony w 1777 do Budy .
W 1891 roku według planów wiedeńskiego architekta Jakoba Gartnera wbudowany został budynek dzisiejszego Centrum Sztuki Współczesnej .
W 1918 miasto zostało utracone przez Węgry na rzecz nowo powstałej Czechosłowacji .
Produkcja samochodów .
W latach 1973–1999 produkowano tu w fabryce TAZ samochód dostawczy Škoda 1203 , który od 1993 roku nazywał się TAZ 1500 .
W roku 2006 PSA ( Peugeot i Citroën ) otworzyło fabrykę samochodów osobowych i są tam m.in .
produkowane Peugeot 207 i Citroën C3 Picasso .
