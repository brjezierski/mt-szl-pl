Historia .
W nagrodę za waleczność mieszkańców podczas walk z Maurami Karol Młot nadał Andorze prawa miejskie .
Następnie stała się ona lennem hrabiów Urgell , aby ostatecznie w 1133 r. przejść pod władanie biskupa z La Seu d ’ Urgell .
W XII wieku była przedmiotem sporu pomiędzy biskupami a ich francuskimi sąsiadami z północy , hrabiami z Foix .
W 1278 r. udało się rozwiązać konflikt – na mocy zawartego porozumienia biskup i hrabia zostali współrządcami .
Andora natomiast zobowiązała się płacić obu stronom daniny : biskupom Urgell w wysokości 460 peset rocznie , hrabiom Foix w wysokości 960 franków rocznie .
W 1419 r. na wniosek ludności Andory został powołany lokalny parlament nazywany Radą Terytorialną ( `` Consell de la Terra '' ) , nazywany również Radą Dwudziestu Czterech , który później został przekształcony w Radę Generalną ( Consell General ) .
W 1607 król Henryk IV , hrabia Foix wydał edykt , na mocy którego przekazał swe prawa do współrządzenia Andorą francuskiemu władcy .
Rewolucja francuska spowodowała utratę zwierzchnictwa Francji nad Andorą – jej jedynym władcą pozostawał biskup Urgell .
Determinacja lokalnej ludności doprowadziła jednak do wydania przez Napoleona I dekretu , na mocy którego zwierzchnik państwa francuskiego ponownie zarządzał Andorą wraz z biskupem .
W okresie 1812–1813 Cesarstwo Francuskie zajęło Katalonię i podzieliło ją na cztery departamenty .
Wówczas Andora weszła w skład jednego z nich ( `` départament de Sègre '' ) .
