Andora
Andora , Księstwo Andory ( kat .
`` Principat d ’ Andorra '' [ ] ) , nazwa historyczna ( tradycyjna ) : `` Doliny Andory '' ( kat .
`` Les Valls d ’ Andorra '' ) – małe państwo w południowo-zachodniej Europie , bez dostępu do morza .
Leży w Pirenejach , granicząc od północy z Francją , a od południa z Hiszpanią .
Geografia .
`` Andora '' to kraj leżący w Pirenejach i całkowicie pokryty górami .
Ma powierzchnię 468 km² .
Średnia wysokość terenu to 1996 m n.p.m .
Na licznych zboczach często występują wysokogórskie łąki i lasy .
Najwyższy punkt w państwie to szczyt góry Pic Alt de la Coma Pedrosa ( 2946 m n.p.m .
) , najniższym jest Riu Runer , który leży w pobliżu granicy z Hiszpanią na wysokości 840 m n.p.m .
Najdłuższą rzeką jest Valira , będąca dopływem hiszpańskiej rzeki Segre .
Andora dzieli się na 7 parafii ( kat .
`` parròquia '' ) .
