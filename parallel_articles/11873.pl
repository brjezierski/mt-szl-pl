Trollface
Trollface ( ) – rysunek stworzony w 2008 przez Carlosa Ramireza będący elementem komiksu z kierowcą i policjantem opublikowanego przez Ramireza pod pseudonimem „ Whynne ” na DeviantArt .
Komiks będący reakcją na zachowanie części użytkowników forum 4chan zrobiono w programie Paint .
„ Twarz trolla ” szybko stała się internetowym memem i symbolem trollowania .
Carlos Ramirez zarobił na memie 100 tysięcy dolarów pochodzących z tytułu licencji i ugód ( stan na kwiecień 2015 ) .
