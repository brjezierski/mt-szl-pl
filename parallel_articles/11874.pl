Trollhättan
Trollhättan – miasto w południowo-zachodniej Szwecji , w regionie Västergötland , nad rzeką Göta älv , w pobliżu jeziora Wener .
Około 44 498 mieszkańców .
Znajdowała się w nim główna fabryka samochodów Saab .
W mieście rozwinął się przemysł lotniczy , maszyn budowlanych , chemiczny oraz hutniczy .
