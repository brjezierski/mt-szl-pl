Trondheim
Trondheim ( dawniej Nidaros oraz Trondhjem ) – miasto w Norwegii , nad Trondheimsfjorden u ujścia rzeki Nidelvy .
Ośrodek administracyjny , dobrze rozwinięty przemysł stoczniowy , maszynowy , metalowy , rybny , ważny port handlowy i rybacki .
Ważny ośrodek sportów zimowych ( trasy biegowe , skocznie narciarskie ) .
Populacja 198 219 osób ( październik 2019 ) .
Miasto , założone w 996 roku przez Olafa Tryggvasona , do 1217 pełniło funkcję stolicy wikińskiej Norwegii .
W latach 1152–1537 było siedzibą katolickiej archidiecezji Nidaros ( obecnie funkcjonuje luterańska Diecezja Nidaros i Nidarosdomen – katedra Nidaros ) .
Gmina w obecnym kształcie istnieje od 1964 roku , kiedy Trondheim zostało połączone z Byneset , Leinstrand , Strinda i Tiller .
