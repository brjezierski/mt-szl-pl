Tropico 3
Tropico 3 – gra komputerowa z gatunku strategii czasu rzeczywistego ( RTS ) , wyprodukowana przez bułgarską firmę Haemimont Games , wydana przez Kalypso Media .
Premiera gry na Windows miała miejsce 20 października 2009 , a na platformę Xbox 360 13 listopada 2009 .
Opis .
Fabuła nawiązuje do pierwszej części gry , natomiast w tej gracz `` podstępnym i podejrzanym sposobem , przejmuje władzę na karaibskiej wysepce Tropico , gdzie obejmuje urząd wszechpotężnego El Presidente '' .
Wyspa ma położenie bardzo strategiczne , dlatego dwa supermocarstwa , USA oraz ZSRR , starają się podporządkować niepodległy kraj dla swoich własnych machinacji .
Podobnie jak pierwsza wersja gry także Tropico 3 posiada ścieżkę dźwiękową w stylu latynoamerykańskim , która nadaje grze odpowiedni charakter .
Tryby rozgrywki .
Autorzy dali do użytku kilka różnych typów gry .
Pierwszy typ gry to kampania , składająca się z 15 oddzielnych , niepowiązanych ( choć w jednej z misji zostaje wspomniana nazwa z innej ) zadań .
Drugim typem jest zabawa , czyli po prostu wolna gra , w której na początku ustalamy poszczególne jej zasady .
Ostatni typ to wyzwania , które tworzone są i rozgrywane w sieci internetowej .
Rozgrywka .
W grze ujęto wiele aspektów , z których każdy jest ważny .
Odpowiednie zastosowanie każdego z nich jest niezbędne , aby osiągnąć wyznaczone cele .
Niewłaściwe działania lub ich brak przyczyniają się do znacznego utrudnienia gry , dlatego nie należy zaniedbywać jakiegokolwiek z nich .
Awatar .
Po uruchomieniu danej misji , pojawia się okienko wyboru Awatara gracza , czyli postaci , którą w danym zadaniu gracz będzie kierował jako El Presidente .
Można utworzyć własną osobę lub wybrać którąś z dostępnych .
Wśród dostępnych postaci znajdują się m.in .
Fidel Castro , Che Guevara , Hernandez Martinez , `` Papa Doc '' Duvalier , Juan Perón , Augusto Pinochet , Antonio Salazar , Anastasio Somoza , Senior , Manuel Noriega , Rafael Trujillo i Eva `` Evita '' Perón , a także kilka fikcyjnych osób .
Wygląd i cechy Awatara .
Tworząc osobę El Presidnete , można wybrać jego wygląd spośród różnorodnych możliwości .
Możliwe jest wybranie płci , koloru skóry , fryzury , kostiumu , nakrycia głowy , akcesoriów , kolczyków ( tylko u kobiet ) lub wąsów i brody ( tylko u mężczyzn ) .
Prócz wyglądu , liczy się wnętrze .
Dlatego autorzy oddali do dyspozycji gracza liczne opcje , które znacząco wpływają na grę .
Umożliwiono wybranie tła , drogi do władzy , dwóch zalet i dwóch wad .
Niektóre tła , drogi do władzy , zalety i wady mogą się wykluczać wzajemnie , dlatego trzeba z rozwagą dobierać poszczególne elementy .
Technika .
W grze został wykorzystany nowy silnik grafiki 3D , przez co ma ona realistyczny wygląd .
Oprócz tego w grze mieszkańcy wyspy mogą prowadzić samochody oraz ciężarówki i limuzyny .
Podobnie jak w pierwszej części , także i w Tropico 3 `` El Presidente '' towarzyszy doradca , który w humorystyczny sposób informuje gracza o wydarzeniach na wyspie .
