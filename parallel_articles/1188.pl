Andreas Wellinger
Andreas Wellinger ( ur .
28 sierpnia 1995 w Traunstein ) – niemiecki skoczek narciarski , reprezentant klubu SC Ruhpolding , członek kadry narodowej .
Dwukrotny mistrz olimpijski : indywidualny na normalnej skoczni z 2018 i drużynowy z 2014 , a także srebrny medalista indywidualnie na skoczni dużej i w drużynie z 2018 .
Mistrz świata w drużynie mieszanej z 2017 i 2023 , indywidualny srebrny medalista ze skoczni normalnej i dużej z 2017 oraz normalnej z 2023 .
Drużynowy srebrny medalista mistrzostw świata w lotach narciarskich z 2016 i 2022 .
Zwycięzca Letniego Grand Prix 2013 i drugi zawodnik z 2016 .
Drużynowy złoty medalista Zimowych Igrzysk Olimpijskich Młodzieży 2012 .
Indywidualny wicemistrz świata juniorów z 2015 , drużynowy srebrny medalista z 2015 oraz brązowy z 2013 .
Medalista mistrzostw kraju .
Przebieg kariery .
Lata 2010–2012 .
