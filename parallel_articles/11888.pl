Troyes
Troyes ( wym . )
– miasto i gmina we Francji , położone nad Sekwaną , w regionie Grand Est , w departamencie Aube , w okręgu ( `` arondissement '' ) Troyes .
Miasto było historyczną stolicą Szampanii .
W 2009 roku gminę zamieszkiwało 62 812 osób a zespół miejski liczył 126 000 mieszkańców .
Zachowane domy o konstrukcji szachulcowej nadają staremu miastu specyficzny charakter .
Posiada ono wiele ciekawych atrakcji turystycznych z XVI – XVIII w .
Kilkanaście kilometrów w kierunku wschodnim znajduje się Lac d ’ Orient , sztuczne jezioro z możliwością uprawiania sportów wodnych , w szczególności żeglarstwa oraz wędkarstwa z kempingiem 300 metrów od piaszczystej plaży .
