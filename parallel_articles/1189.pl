Andrej Hlinka
Andrej Hlinka ( ur .
27 września 1864 w Černovej , zm .
16 sierpnia 1938 w Rużomberku ) – słowacki polityk o orientacji narodowo-klerykalnej , publicysta , ksiądz rzymskokatolicki .
Współzałożyciel i wieloletni przywódca Słowackiej Partii Ludowej , uznawany przez Słowaków za ojca narodu .
Order jego imienia jest jednym z najwyższych odznaczeń państwowych Słowacji .
Życiorys .
Urodził się w miejscowości Černova ( obecnie część Rużomberka ) w Królestwie Węgier , w wielodzietnej rodzinie chłopskiej .
Od 1877 do 1880 uczęszczał do gimnazjum w Ružomberku , w latach 1881–1883 kontynuował naukę w gimnazjum w Lewoczy .
W 1883 został przyjęty do seminarium duchownego w Spišskej Kapitule , a 19 czerwca 1889 przyjął święcenia kapłańskie z rąk biskupa Juraja Császku .
W 1890 ks .
Hlinka objął swoją pierwszą parafię we wsi Liptovské Kľačany , gdzie podjął działalność społeczną i kulturalną , działał jako publicysta .
W 1897 został redaktorem pisma `` Ľudove noviny '' , wydawanego w Rużomberku , które to stanowisko zajmował do 1901 .
W 1898 startował bez powodzenia w wyborach z ramienia partii ludowej ; w 1901 opuścił jej szeregi .
Od 1905 był proboszczem w Martinie , gdzie prowadził działalność polityczną , opowiadając się za katolicką partią Węgier ( `` Katolikus Néppárt '' ) .
