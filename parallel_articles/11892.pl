Trumna
Trumna ( łac .
tumba ) – skrzynia drewniana lub metalowa , często wykładana materiałem i koronkami , służąca do pochówku zmarłych ludzi .
Trumny służą też nocą jako miejsce snu ( np .
w zakonie sióstr klarysek ) , a za dnia jako miejsce do siedzenia .
W kremacji bardzo często stosuje się trumny wykonane z kartonu ( trumna kremacyjna ) lub trumny słomiane .
Wyraz trumna pisany w starszych postaciach jako `` truna '' , `` trunna '' , pochodzi z języka średnio-wysoko-niemieckiego `` Truhe '' , `` Truhne '' – „ skrzynia ” .
Historia .
Pierwsze przypadki grzebania zmarłych w drewnianych skrzyniach odnotowano na początku średniowiecza ( głównie władców ) .
W tradycji polskiej według Długosza , pierwszym królem złożonym do grobu w trumnie był Bolesław Chrobry , który ma spoczywać pod Złotą Kaplicą w poznańskiej katedrze .
Z tego samego okresu pochodzą resztki trumien odkrytych na wikińskim cmentarzysku w Bodzi .
W Europie zwyczaj pochówku w trumnach upowszechnił się w XIV wieku .
Naukowcy sądzą , że związane to było z epidemią dżumy , która w połowie XIV wieku panowała w Europie .
Mogły stanowić one dodatkowe zabezpieczenie przed rozprzestrzenianiem się choroby .
Stosowano wówczas trumny sosnowe , dębowe , malowane lub obciągane czarnym materiałem .
