Piotrków Trybunalski
Piotrków Trybunalski – miasto na prawach powiatu w centralnej Polsce .
Piotrków był miastem królewskim w starostwie piotrkowskim w powiecie piotrkowskim województwa sieradzkiego w końcu XVI wieku .
Dopełnienie nazwy o przymiotnik `` Trybunalski '' nastąpiło po II wojnie światowej .
W latach 1975–1998 miasto było stolicą województwa piotrkowskiego .
Według danych GUS z 31 grudnia 2021 r. , Piotrków Trybunalski liczył 68 270 mieszkańców i był pod względem liczby ludności drugim ( po Łodzi ) miastem w województwie łódzkim , a także 50. spośród najludniejszych miast w Polsce .
Toponimia .
Nazwa miasta notowana jest od XIII wieku jako `` Piotrków '' i `` Pietrków '' , od XIV wieku spotyka się także `` Piotrkowo '' .
Pochodzi ona od imienia `` Piotr '' , zdrobnionego do `` Piotrek '' , w języku staropolskim także `` Pietrek '' .
