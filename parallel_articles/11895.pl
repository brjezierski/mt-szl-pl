Trypolis ( Liban )
Trypolis ( , ) – miasto w północnym Libanie , nad Morzem Śródziemnym .
Stolica dystryktu Kada Trypolis , zamieszkane przez 237 tysięcy mieszkańców , z czego 80 % to muzułmanie-sunnici .
Drugie pod względem wielkości miasto Libanu , ważny port morski : przywóz ropy naftowej , wywóz wyrobów przemysłu lekkiego i przetwórczego ropy naftowej , przemysł włókienniczy , spożywczy , chemiczny , rozwinięte również rzemiosło oraz połowy ryb i gąbek .
Historia .
Miasto założone przez Fenicjan na terenie obecnego portu , od IV wieku p.n.e .
stolica federacji miast-państw fenickich : Sydonu , Tyru i Arwadu .
Kolejno pod władzą Seleucydów , Rzymian i Arabów ( od VII wieku , czasy intensywnego rozwoju , zwłaszcza za panowania egipskiej dynastii Fatymidów od XI wieku ) .
W 1109 zajęte przez krzyżowców po 5 letnim oblężeniu w czasie I krucjaty , stolica hrabstwa Trypolisu .
W 1289 port zdobyty i zniszczony przez wojska mameluckiego sułtana Kalawuna , który nakazał zbudować nowe miasto w sąsiedztwie .
Od 1516 do czasu I wojny światowej w granicach Imperium Osmańskiego .
W czasie I wojny okupowane przez wojska brytyjskie , zaś w 1920 włączone do francuskiego mandatu Ligi Narodów .
W czasie II wojny światowej zajęte przez wojska brytyjskie i siły Wolnych Francuzów do 1946 .
Od 1982 siedziba Organizacji Wyzwolenia Palestyny .
W następnym roku jej przywódca – Jasir Arafat – w wyniku walk wewnętrznych został zmuszony do opuszczenia Trypolisu .
Później miasto pod kontrolą wojsk syryjskich .
Zabytki .
Ruiny twierdzy krzyżowców ( XII w. ) ; wielki meczet ( XII w. , dawna katedra ) ; meczet Tajnal ( XIV w. , dawny kościół Karmelitów ) , medresy , bazary , mauzolea ( XIV w . )
