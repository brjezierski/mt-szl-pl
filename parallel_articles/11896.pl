Trynidad i Tobago
Trynidad i Tobago ( ang .
`` Trinidad and Tobago '' ) – wyspiarskie państwo Ameryki Południowej w archipelagu Małych Antyli .
Obejmuje dwie główne wyspy , Trynidad i Tobago , oraz szereg mniejszych .
Geografia .
Państwo leży na dwóch wyspach .
Trynidad , większa i położona bliżej wybrzeża Wenezueli , stanowi pod względem geologicznym przedłużenie Gór Karaibskich z północno-wschodniej Wenezueli .
Na wyspie przebiegają równoleżnikowo trzy pasma górskie , z których najwyższe są krystaliczne Góry Północne , sięgające ponad 900 m n.p.m .
( El Cerro del Aripo – 940 m ) .
Opadają one stromo ku wybrzeżu , płynące w nich rzeki tworzą liczne wodospady .
Położone w centralnej części wyspy Góry Środkowe i bardziej na południe Góry Południowe , zbudowane z wapieni i piaskowców , sięgają do wysokości około 300 m n.p.m .
Między tymi dwoma pasami rozpościera się obniżenie , w którym występują wulkany błotne oraz naturalny wypływ smoły tzw .
Jezioro Asfaltowe ( `` Pitch Lake '' ) .
Najwyższy szczyt mniejszej wyspy Tobago sięga 576 m n.p.m .
Na wyspach panuje gorący i wilgotny klimat równikowy , łagodzony jednak przez wiatry pasatowe .
