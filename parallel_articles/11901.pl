Trzebiatów
Trzebiatów ( niem .
`` Treptow an der Rega '' ) – miasto w północno-zachodniej Polsce , w woj .
zachodniopomorskim , w powiecie gryfickim , siedziba miejsko-wiejskiej gminy Trzebiatów .
Leży na Pobrzeżu Szczecińskim , nad rzeką Regą , w odległości ok. 8 km od Morza Bałtyckiego .
Według danych z 30 czerwca 2010 miasto miało 10 165 mieszkańców , co lokuje Trzebiatów na 25. miejscu pod względem ludności w województwie .
Miasto jest siedzibą administracyjną gminy Trzebiatów .
Jest ośrodkiem miejskim o tradycji średniowiecznej , dawnym miastem hanzeatyckim , które konkurowało jako ośrodek handlu międzynarodowego z Gryficami i Kołobrzegiem .
Trzebiatów jest miejscem produkcji wanien i łodzi motorowodnych , a także przetwórstwa rybnego .
Miasto ma czytelne średniowieczne rozplanowanie w obrębie częściowo zachowanych ceglanych murów obronnych , opasanych fosą , z gotycką farą , klasycystycznym pałacem , ratuszem na rynku oraz licznym zespołem mieszczańskich kamienic .
Trzebiatowski kościół rzymskokatolicki pełni funkcję Archidiecezjalnego Sanktuarium Maryjnego Macierzyństwa NMP .
Znajduje się w nim koronowany obraz Madonny z Dzieciątkiem z 1867 roku .
