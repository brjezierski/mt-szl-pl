Trzebieszów ( Moszczanka )
Trzebieszów ( , ) – część wsi Moszczanka w Polsce położonej w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Trzebieszów położony jest w południowo-wschodniej części Moszczanki , przy drodze prowadzącej do Pokrzywnej .
W 1927 miejscowość zamieszkiwało 117 osób , a w 1939 – 175 .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
Dawna , niemiecka nazwa `` Sichtigfür '' względnie `` Sichdichfür '' tłumaczona jest przez regionalistów jako `` Bacz-na-się '' ( czeskie `` Dej pozor '' ) , co miało odnosić się do pobliskiej granicy .
2 kwietnia 1949 r. nadano miejscowości , wówczas administracyjnie związanej z Moszczanką , polską nazwę `` Trzebieszów '' .
