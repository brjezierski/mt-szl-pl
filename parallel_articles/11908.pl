Grochowo Trzecie
Grochowo Trzecie ( niem .
`` Schweinekampe '' ) – osada w Polsce położona w województwie pomorskim , w powiecie nowodworskim , w gminie Sztutowo na obszarze Żuław Wiślanych .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
Inne miejscowości o nazwie Grochowo : Grochowo Drugie , Grochowo Pierwsze , Grochowo
