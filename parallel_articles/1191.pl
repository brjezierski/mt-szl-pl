Andrij Sadowy
Andrij Iwanowycz Sadowy ( ukr .
Андрій Іванович Садовий ; ur .
19 sierpnia 1968 we Lwowie ) – ukraiński inżynier , działacz społeczny i samorządowiec , od 2006 mer Lwowa , w latach 2013–2019 przewodniczący partii „ Samopomoc ” .
Życiorys .
Po zakończeniu nauki w Technikum Radioelektronicznym we Lwowie studiował na Politechnice Lwowskiej , którą ukończył z tytułem inżyniera elektrotechniki ( 1995 ) i ekonomisty ze specjalnością finanse i kredyty ( 1997 ) .
W 1999 ukończył studia w Akademii Administracji Państwowej przy Prezydencie Ukrainy ( Академія державного управління при Президентові України ) .
W 1989 rozpoczął pracę jako elektrotechnik .
Od 1992 do 1995 zastępował dyrektora lwowskiego oddziału Funduszu Przystosowania Społecznego Młodzieży przy Radzie Ministrów Ukrainy .
W 1997 objął stanowisko przewodniczącego rady oraz zarządu przedsiębiorstwa „ Piwdeńzachidełektromereżbud ” ( do 2005 ) .
W latach 1997–2001 pełnił obowiązki przewodniczącego zarządu Fundacji Rozwoju Ziemi Lwowskiej ( ukr .
Фонд розвитку Львівщини ) .
W 2002 został przewodniczącym rady spółki „ Tełeradiokompanija Luks ” .
W latach 2002–2003 szefował Instytutowi Rozwoju Miasta .
Od 2005 stoi na czele Zjednoczenia Społecznego Samopomicz .
W 1998 rozpoczął karierę samorządowca zdobywając mandat radnego w Radzie Miejskiej Lwowa .
Objął stanowisko przewodniczącego komisji polityki gospodarczej ( do 2002 ) .
26 marca 2006 wybrano go na prezydenta miasta .
W wyborach w 2010 uzyskał reelekcję na to stanowisko .
Nadal pełni liczne funkcje społeczne .
Jest przewodniczącym Rady Nadzorczej Funduszu Kultury i Sztuki im .
Metropolity Andrzeja Szeptyckiego .
W 2003 wybrano go na wiceprzewodniczącego Rady Instytutu „ Mosty na Wschód ” .
Zasiada w Polsko-Ukraińskiej Kapitule Pojednania .
Do 2009 należał do partii Związek Ludowy „ Nasza Ukraina ” .
Od 2013 był przewodniczącym założonej przez siebie partii „ Samopomoc ” .
Jego żoną jest Kateryna Kit .
Mają czwórkę synów : Iwana , Tadeja , Mychajła i Josypa .
