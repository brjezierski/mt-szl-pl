Trzy karty
Trzy karty , trzy kubki , żargonowo benkle lub benkiel – rodzaj gry hazardowej , często łączącej się z oszustwem .
Opis .
Prowadzący grę posługuje się trzema kartami lub trzema kubkami .
Tasując karty lub zmieniając błyskawicznie ustawienie kubków , daje graczowi – po wpłaceniu określonej kwoty ( ustalanej przez benklarza arbitralnie ) – za zadanie wskazanie wygrywającej karty albo kubka skrywającego kulkę lub monetę .
Najczęściej oszust z kilkoma wspólnikami pozoruje grę , w której jest „ ogrywany ” przez wspólników .
Dzieje się tak do momentu , gdy na grę zdecydują się przypadkowe osoby .
Początkowo i one wygrywają , dopóki nie zaczną stawiać większych kwot .
Według Jędrzeja Kitowicza za panowania Augusta III w nieuczciwej grze w trzy kubki specjalizowali się w Warszawie żołnierze Gwardii Koronnej Pieszej .
