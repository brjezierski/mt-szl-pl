Trójkąt
Trójkąt – wielokąt o trzech bokach .
Trójkąt to najmniejsza ( w sensie inkluzji ) figura wypukła i domknięta , zawierająca pewne trzy ustalone i niewspółliniowe punkty płaszczyzny ( otoczka wypukła wspomnianych trzech punktów ) .
Odcinki tworzące łamaną nazywamy bokami , punkty wspólne sąsiednich boków nazywamy wierzchołkami trójkąta .
Każdy trójkąt jest jednoznacznie wyznaczony przez swoje wierzchołki .
Często dla wygody jeden z boków trójkąta nazywa się podstawą , a pozostałe – ramionami .
W każdym trójkącie suma miar kątów wewnętrznych między bokami wynosi 180° , zaś długości boków muszą spełniać pewne zależności ( patrz dalej ) .
Rodzaje .
Trójkąty można dzielić ze względu na długości ich boków oraz ze względu na miary ich kątów .
