Migdałowiec trójklapowy
Migdałowiec trójklapowy , migdałek trójklapowy ( `` Prunus triloba '' Lindl . )
– gatunek rośliny krzewiastej , należący do rodzaju `` Prunus '' .
Występuje w Chinach i Korei .
Introdukowany do Stanów Zjednoczonych .
Nazwa `` trójklapowy '' wywodzi się od kształtu liści .
Zastosowanie .
Roślina ozdobna – w uprawie oprócz formy krzewiastej występują formy drzewiaste szczepione na podkładce ‘ Multiplex ’ .
Mają kulistą koronę i wysokość ok. 150 cm .
Może być uprawiany na obszarach o łagodnym klimacie , jego kwiaty są bowiem wrażliwe na wiosenne przymrozki .
U form szczepionych na pniu często pojawiają się dzikie odrosty , które należy usuwać .
Aby obficie kwitnął , należy go sadzić na żyznej glebie i na słonecznym stanowisku .
W czerwcu , po przekwitnięciu rośliny jego pędy należy silnie przyciąć , by korona uległa zagęszczeniu , nie przycinany migdałek bowiem wytwarza luźną i słabo kwitnącą koronę .
