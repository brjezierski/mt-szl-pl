Trzęsienie ziemi na Haiti ( 2021 )
Trzęsienie ziemi na Haiti w 2021 roku – trzęsienie ziemi o magnitudzie 7,2 w skali Richtera , którego epicentrum znajdowało się w odległości ok. 150 km od Port-au-Prince w Haiti .
Główny wstrząs nastąpił 14 sierpnia 2021 o godz .
8:30 czasu lokalnego .
W wyniku kataklizmu śmierć poniosło 2248 osób , co czyni go drugim najtragiczniejszym trzęsieniem ziemi w nowożytnej historii Haiti po trzęsieniu ziemi z 2010 roku , w którym zginęło 316 tysięcy osób .
Według USGS epicentrum wstrząsów znajdowało się w rejonie miejscowości Petit Trou de Nippes .
Hipocentrum znajdowało się na głębokości 10 km .
Zniszczonych zostało blisko 60 tysięcy budynków , a kolejnych 76 tysięcy zostało uszkodzonych .
Przez następne 24 godziny odnotowano pięć wstrząsów wtórnych .
W wyniku kataklizmu , najbardziej ucierpiało miasto Les Cayes , gdzie zniszczonych lub uszkodzonych została większość budynków .
Wśród ofiar śmiertelnych znalazł się były burmistrz miasta Gabriel Fortuné .
Trzęsienie zniszczyło całkowicie sześć szpitali .
Wiele ofiar śmiertelnych odnotowano w kościołach , gdyż wstrząsy nastąpiły w porze porannych mszy .
Zaraz po trzęsieniu ziemi haitański rząd wprowadził stan wyjątkowy w całym kraju .
Z wielu krajów na Haiti dotarła pomoc humanitarna .
Akcja humanitarną utrudniły działania gangów , których członkowie dokonali napadów na transporty z żywnością oraz sprzętem medycznym .
Straty gospodarcze spowodowane tym trzęsieniem ziemi szacuje się na ponad 1,5 miliarda dolarów , co stanowi ponad 10 % gospodarki Haiti .
