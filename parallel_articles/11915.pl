Trzęsienie ziemi w Shaanxi ( 1556 )
Trzęsienie ziemi w 1556 roku w Shaanxi ( , pinyin : Huà xiàn dà dìzhèn ) było najbardziej katastrofalnym w skutkach trzęsieniem ziemi , jakie odnotowano w historii .
Zginęło w nim ok. 830 tys .
ludzi .
Miało miejsce rankiem 23 stycznia 1556 w chińskiej prowincji Shaanxi , lecz jego skutki odczuli także mieszkańcy prowincji Shanxi , Henan , Gansu , Hebei , Szantung , Hubei , Hunan , Jiangsu i Anhui .
Zniszczenia odnotowano w pasie szerokim na 840 km ; w niektórych powiatach zginęło powyżej 60 % mieszkańców .
Najwięcej ofiar było w regionach , w których zamieszkiwane były zbocza wzgórz zbudowanych z ziemi lessowej , które obsuwając się burzyły postawione na nich domy , oraz tam , gdzie zamieszkiwane były jaskinie ( yaodong ) , które zawaliły się od wstrząsów .
Przez 6 miesięcy były odczuwane wstrząsy wtórne .
