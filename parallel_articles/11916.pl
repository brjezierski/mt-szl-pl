Trzęsienie ziemi
Trzęsienie ziemi – gwałtowne rozładowanie naprężeń w skorupie ziemskiej połączone z ruchem warstw skalnych .
W przypadku trzęsienia tektonicznego jest to ruch wzdłuż uskoku .
Nagromadzenie naprężeń jest często wynikiem przejściowego zablokowania ruchu skał wzdłuż tego uskoku .
Uwalniająca się przy tym energia w około 20-30 % rozchodzi się w postaci fal sejsmicznych , z których część dociera na powierzchnię Ziemi w postaci niszczących fal powierzchniowych .
Pozostała część energii zamienia się w ciepło lub trwałe deformacje skał .
Podstawowe zagadnienia związane z trzęsieniami ziemi .
Ognisko trzęsienia ziemi – tj .
miejsce , gdzie zapoczątkowane zostało uwalnianie energii nagromadzonej na linii uskoku .
Należy przy tym pamiętać , iż jest to punkt czysto teoretyczny ( ang . ''
focus '' = hipocentrum ) , ponieważ faktycznie energia wstrząsu rozładowuje się zawsze na pewnej powierzchni uskoku .
Epicentrum trzęsienia ziemi określa się miejsce na powierzchni ziemi , położone bezpośrednio nad ogniskiem wstrząsu .
Punkt ten oznacza miejsce , gdzie fale sejsmiczne rozchodzące się z ogniska we wszystkich kierunkach najszybciej osiągają powierzchnię .
Zazwyczaj właśnie w epicentrum fale posiadają największą siłę niszczącą i wprawiają podłoże w największy stopień wibracji ( jest to tzw .
obszar epicentralny ) .
Nie stanowi to jednak reguły , gdyż intensywność wstrząsów w dużej mierze zależy od uwarunkowań geologicznych i rodzaju podłoża ( np .
położenie miasta na terenie bagnistym znacznie intensyfikuje drgania ) .
Falą sejsmiczną nazywamy pojedynczą falę wibracji , w jakiej nagromadzona energia jest uwolniona w trakcie ruchów mas skalnych na linii uskoku .
