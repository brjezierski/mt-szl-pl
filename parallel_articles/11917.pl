Wiśnia ptasia
Wiśnia ptasia , wiśnia dzika , czereśnia , trześnia ( `` Prunus avium '' L. ) – gatunek rośliny należący do rodziny różowatych .
Rodzime obszary jej występowania to Europa oraz Azja Zachodnia i Kaukaz , rozprzestrzeniła się też gdzieniegdzie poza te obszary .
W Polsce rośnie dziko na terenie całego kraju , od nizin po pogórze i niższe tereny górskie ( po 700–900 m n.p.m. , w Gorcach spotykana nawet powyżej 1000 m n.p.m .
) , jednak głównie na południu kraju .
Jest w wielu odmianach powszechnie uprawiana jako drzewo owocowe .
Biologia i ekologia .
Występuje w lasach liściastych , lasach mieszanych , w zadrzewieniach śródpolnych i w czyżniach .
W klasyfikacji zbiorowisk roślinnych gatunek charakterystyczny dla All .
`` Carpinion '' .
Lubi żyzne gleby bogate w węglan wapnia , powstałe z glin i lessów .
W górach rośnie jednak także na glebach szkieletowych na podłożu z fliszu karpackiego .
Preferuje miejsca o dużej wilgotności gleby .
Cechuje ją światłożądność – stąd szybki wzrost w okresie młodocianym i w pierwszej połowie życia .
Dożywa zazwyczaj 80–100 lat .
Kwitnie od kwietnia do maja .
Owoce stanowią poszukiwany pokarm dla wielu gatunków zwierząt .
Często zjadane są przez ptaki , które przyczyniają się do rozsiewania czereśni ( ornitochoria ) .
Najgrubsza czereśnia w Polsce ( na terenie Lasów Państwowych ) rośnie w Myscowej , na stokach góry Polana w Beskidzie Niskim i ma 356 cm obwodu i 22 metry wysokości .
Zmienność .
Tworzy mieszańce z wiśnią pospolitą i wiśnią karłowatą .
Zastosowanie .
Odmiany czereśni według pory dojrzewania dzieli się tradycyjnie na wczesne , o średniej porze dojrzewania i późne .
Niezależnie od głównego podziału często spotyka się podział według tygodni dojrzewania , gdzie odmianami od których liczy się tygodnie są najwcześniejsze w Polsce stare odmiany ‘ Marchijska ’ lub ‘ Rivan ’ .
Najczęściej dojrzewają one w Polsce na początku czerwca , a cały okres dojrzewania czereśni trwa 8–10 tygodni .
