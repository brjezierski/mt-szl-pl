Android ( singel TVXQ )
ANDROID – trzydziesty piąty singel południowokoreańskiego zespołu TVXQ , wydany w Japonii 11 lipca 2012 roku przez Avex Trax .
Został wydany w trzech edycjach : regularnej CD , limitowanej CD+DVD oraz „ Bigeast ” .
Osiągnął 1 pozycję w rankingu Oricon i pozostał na liście przez 9 tygodni , sprzedał się w nakładzie egzemplarzy w Japonii .
Singel zdobył status złotej płyty .
