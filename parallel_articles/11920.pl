Szklarka Trzcielska ( przystanek kolejowy )
Szklarka Trzcielska – zlikwidowany przystanek osobowy w Szklarce Trzcielskiej na trasie linii kolejowej nr 373 Międzychód – Zbąszyń , w województwie wielkopolskim w Polsce .
Przystanek został zamknięty w 1987 , a fizycznie zlikwidowany przed 28 listopada 2005 roku .
