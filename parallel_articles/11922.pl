Três Corações
Três Corações – miejscowość i gmina w Brazylii , w stanie Minas Gerais .
Według danych z 2009 roku powierzchnia miasta wynosiła 825,9 km² , a liczba jego mieszkańców 75 776 osób .
W mieście tym urodził się Pelé .
Ulicę , na której się urodził , nazwano Rua Edson Arantes do Nascimento .
