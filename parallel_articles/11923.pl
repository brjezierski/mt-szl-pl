Tsuchiura
– miasto w Japonii , w prefekturze Ibaraki , na wyspie Honsiu .
Położone jest nad jeziorem Kasumigaura .
Ma powierzchnię 122,89 km2 .
W 2020 r. mieszkało w nim 142 160 osób , w 62 693 gospodarstwach domowych ( w 2010 r. 143 023 osoby , w 56 674 gospodarstwach domowych ) .
Przemysł .
W mieście rozwinął się przemysł maszynowy , środków transportu oraz rybny .
Historia .
Wraz z utworzeniem nowego systemu administracyjnego 1 kwietnia 1889 roku w powiecie Niihari powstała miejscowość Tsuchiura .
