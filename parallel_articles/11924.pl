Trąbka francuska
Trąbka francuska ( `` Tubaria romagnesiana '' Arnolds ) – gatunek grzybów należący do rodziny `` Tubariaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tubaria , Tubariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską zaproponował Władysław Wojewoda w 2003 roku .
Występowanie .
Znane jest występowanie trąbki francuskiej w niektórych krajach Europy .
W piśmiennictwie naukowym na terenie Polski do 2003 r. podano dwa stanowiska ( Poznań 1998 i Turew 2002 ) .
Jej rozprzestrzenienie w Polsce i częstość występowania nie są znane .
Saprotrof .
W Polsce zanotowano występowanie w ogrodzie botanicznych i w zaroślach , na ziemi , wśród mchów i traw .
