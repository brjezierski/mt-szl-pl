Tuber bituminatum
Tuber bituminatum Berk .
& amp ; Broome – gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Morfologia .
Kulistawy lub jajowaty , o regularnym pokroju .
Okrywa jest czarna , a jej powierzchnia pokryta jest wielokątnymi brodawkami .
Obłocznia żyłkowana , o luźnej konsystencji i charakterystycznej dla tego gatunku smolistej woni .
Worki o jajowatym pokroju , na długich podstawach .
Jajowate , brązowe .
Znaczenie .
Grzyby mikoryzowy , rozwijający się w glebie i wytwarzający podziemne owocniki ( askokarpy ) .
