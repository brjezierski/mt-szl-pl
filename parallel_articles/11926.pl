Tuber bonnetii
Tuber bonnetii Roum .
– gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Podziemny grzyb mykoryzowy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Casimir Roumeguère w 1882 r/
Morfologia .
Kulistawy , o nieregularnym pokroju .
Okrywa ma brązowy odcień , a jej powierzchnia jest błyszcząca i brodawkowana .
Miąższ o smaku i woni grzybowych .
Jajowate , kolczaste , o wymiarach 19–22 × 9–12 μm .
