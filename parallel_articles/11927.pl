Trufla Borcha
Trufla Borcha , trufla biaława ( `` Tuber borchii '' Vittad . )
– gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy nazwy naukowej :
Nazwa polska według checklist A. Chmiel .
Morfologia .
Organizmy mikoryzowe , rozwijające się w glebie i wytwarzające jadalne , bulwiaste , podziemne owocniki ( askokarpy ) .
Okrywa i obłocznia owocników jest barwy białawej , żółtawej lub brązowawej .
Występowanie .
Występowanie trufli białawej potwierdzono w : Danii , Hiszpanii , Maroku , Słowenii , Węgrzech , Wielkiej Brytanii , Włoszech i Stanach Zjednoczonych .
Występuje również w Polsce .
