Trufla zimowa
Trufla zimowa ( `` Tuber brumale '' Vittad . )
– gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Grzyb podziemny .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten został po raz pierwszy zdiagnozowany taksonomicznie przez Carlo Vittadiniego w `` Monographia Tuberacearum '' w 1831 r. Zaklasyfikowano tam rodzaj `` Tuber '' do sekcji `` Centrales '' rodziny `` Tubereae '' , należącej do podrzędu `` Angiogasrum '' w ówczesnej klasie `` Gasteromycetum '' .
Gatunek `` Tuber brumale '' został tam opisany m.in .
jako `` Globosum , nigrum , verrucis rotundato-angulosis , [ ... ] Odor fortis ligni Corni sanguineae !
sapor gratus .
`` .
Morfologia .
Pokroju kulistawego , spłaszczony , średnicy do 1,6 cm i wysokości 0,8 cm .
Okrywa mocno przytwierdzona do obłoczni , u młodych owocników czerwonawa , po dojrzeniu czarnobrązowa , pokryta brodawkami o wielkości 2-3 mm .
Obłocznia początkowo wodnista , brudnobrązowawa , u dojrzałych owocników mięsista , szarobrązowa , o marmurkowym wzorze na przekroju .
Worki o wymiarach 62,5–67,5×40–60 μm , elipsoidalne , niemal kuliste , zawierające od 1 do 6 askospor każdy .
Elipsoidalne , o wymiarach przeważnie 22.5–30×15–20 μm , początkowo bezbarwne ( hialinowe ) , potem żółtobrązowe , pokryte cyjanofilnymi kolcami o długości 3–5 μm i szerokości 1 μm .
Występowanie .
Występowanie trufli zimowej potwierdzono w : Bułgarii , Hiszpanii , Słowenii , Włoszech i Stanach Zjednoczonych .
Znaczenie .
Grzyb mikoryzowy , rozwijający się w glebie i wytwarzający jadalne , bulwiaste , podziemne owocniki ( askokarpy ) .
