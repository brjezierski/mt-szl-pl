Trufla dębowa
Trufla dębowa ( `` Tuber dryophilum '' Tul .
& amp ; C .
Tul . )
– gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Nazwa polska według checklist A. Chmiel .
Występowanie .
W Polsce gatunek rzadki .
Znajduje się na `` Czerwonej liście roślin i grzybów Polski '' .
Ma status R – potencjalnie zagrożony z powodu ograniczonego zasięgu geograficznego i małych obszarów siedliskowych .
