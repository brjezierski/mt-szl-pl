Andrychów
Andrychów ( dawniej też `` Jędr ( z ) ychów '' , ) – miasto w południowej Polsce , w województwie małopolskim , w powiecie wadowickim , nad rzeką Wieprzówką , siedziba władz gminy miejsko-wiejskiej Andrychów .
Miasto posiada tradycje przemysłowe sięgające XVIII w. Pokonało problemy związane z transformacją ustrojową i obecnie rozwija się również jako ośrodek przemysłowy : m.in .
powstała tam podstrefa specjalnej strefy ekonomicznej zarządzana przez Krakowski Park Technologiczny .
Tradycyjne gałęzie przemysłu andrychowskiego to przemysł bawełniany , przemysł włókienniczy i przemysł maszynowy .
Z racji położenia , tradycji letniskowej i stale rozwijanego zaplecza stał się ważnym ośrodkiem turystycznym w rejonie Beskidu Małego .
Położenie i komunikacja .
Miasto położone w Kotlinie Andrychowskiej , w Beskidzie Małym , u stóp Pańskiej Góry , nad rzeką Wieprzówką .
Andrychów położony jest wzdłuż :
Przez miasto biegnie Linia kolejowa nr 117 z Bielska-Białej do Kalwarii Zebrzydowskiej Lanckorony obsługiwana przez PolRegio .
W Andrychowie znajdują się dwie stacje kolejowe : Andrychów i Andrychów Górnica .
Komunikację miejską zapewnia Międzygminny Zakład Komunikacyjny w Kętach , który prowadzi działalność przewozową osób na terenie miasta i gminy Andrychów , Kęty oraz Porąbka .
