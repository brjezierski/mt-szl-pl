Trufla wydrążona
Trufla wydrążona ( `` Tuber excavatum '' Vittad . )
– gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten został po raz pierwszy zdiagnozowany taksonomicznie przez Carlo Vittadiniego w `` Monographia Tuberacearum '' w 1831 r. Rodzaj `` Tuber '' zaklasyfikowano tam do sekcji `` Centrales '' rodziny `` Tubereae '' , należącej do podrzędu `` Angiogasrum '' w ówczesnej klasie `` Gasteromycetum '' .
Gatunek `` Tuber excavatum '' został tam opisany m.in .
jako `` Subglobosum , laeve , papillosum , cavum , [ ... ] Odor levis , sapor subnullus '' .
Morfologia .
Pokroju kulistawego , spłaszczony , średnicy ok. 2 cm i wysokości 0,8 cm , z wgłębieniem na środku .
Okrywa owocnika u młodych owocników żółtobrązowa , po dojrzeniu oliwkowobrązowa , pokryta małymi brodawkami .
Obłocznia początkowo biała , potem zółknąca , ostatecznie ciemnobrązowa , z kremowymi pasmami promieniście rozchodzącymi się od środkowego wgłębienia .
Worki o wymiarach 92,5–125×65–80 μm , niemal kuliste , zawierające od 2 do 4 ( czasami do 6 ) askospor każdy .
Elipsoidalne ( czasami szeroko ) , o wymiarach przeważnie 32,5–37,5×25–27,5 μm , z podwójną , usiatkowaną ścianą , początkowo żółtobrązowe , po dojrzeniu brązowe , nieregularnie pokryte kolcami o długości 3–7,5 μm i szerokości 1–1,5 μm .
Występowanie .
Występowanie trufli wydrążonej potwierdzono w : Austrii , Bułgarii , Czechach , Danii , Francji , Hiszpanii , Niemczech , Portugalii , Wielkiej Brytanii i Włoszech .
Znaczenie .
Grzyb mikoryzowy , rozwijający się w glebie i wytwarzający jadalne , bulwiaste , podziemne owocniki ( askokarpy ) .
