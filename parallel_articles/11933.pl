Trufla wielkozarodnikowa
Trufla wielkozarodnikowa ( `` Tuber macrosporum '' Vittad . )
– gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek `` Tuber macrosporum '' został po raz pierwszy zdiagnozowany taksonomicznie przez Carlo Vittadiniego w `` Monographia Tuberacearum '' w 1831 r. Zaklasyfikowano tam rodzaj `` Tuber '' do sekcji `` Centrales '' rodziny `` Tubereae '' , należącej do podrzędu `` Angiogastrum '' w ówczesnej klasie `` Gasteromycetum '' .
Morfologia .
Kulistawy , zazwyczaj o średnicy 0,5 – 2 cm , czasem dochodzi do średnicy 5 cm .
Okrywa chropowata , rdzawobrązowa lub czarnobrązowa .
Hymenium u dojrzałych owocników oliwkowoszare z białawym , marmurkowatym wzorem na przekroju .
Miąższ o czosnkowej lub cebulowej woni .
Eliptyczne , o wymiarach 45–80×25–60 μm , pokryte siatkowym wzorem .
Występowanie .
Gatunek ten rozwija się i tworzy mikoryzę z dębami ( `` Quercus sp .
`` ) .
Znaczenie .
Grzyb mikoryzowy , rozwijający się w glebie i wytwarzający bulwiaste , podziemne owocniki .
Nie są one toksyczne dla człowieka , jednak w przeciwieństwie do niektórych innych gatunków trufli nie mają wysokich walorów smakowych , a niektórzy autorzy uważają je nawet za niejadalne .
Często przez podobieństwo do trufli czarnozarodnikowej ( `` Tuber melanosporum '' ) i trufli letniej ( `` Tuber aestivum '' ) jest sprzedawana po zawyżonych cenach .
