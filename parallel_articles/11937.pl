Tuber separans
Tuber separans Gilkey – gatunek grzybów należący do rodziny truflowatych ( `` Tuberaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tuber , Tuberaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisały został przez Helen Margaret Gilkey w 1916 r .
