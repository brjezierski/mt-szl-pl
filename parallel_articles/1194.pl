Andrzej Czaja
Andrzej Konrad Czaja ( ur .
12 grudnia 1963 w Oleśnie ) – polski duchowny rzymskokatolicki , doktor habilitowany nauk teologicznych , biskup diecezjalny opolski od 2009 .
Życiorys .
Urodził się 12 grudnia 1963 w Oleśnie .
W latach 1978–1982 kształcił się w Liceum Ogólnokształcącym im .
Lotników Polskich w Oleśnie , gdzie zdał egzamin dojrzałości .
W latach 1982–1988 studiował w Seminarium Śląska Opolskiego w Nysie .
Święcenia diakonatu otrzymał 25 października 1987 w Nysie przez posługę Gerarda Kusza , biskupa pomocniczego opolskiego .
