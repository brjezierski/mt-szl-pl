Tuczempy
Tuczempy – wieś w Polsce , położona w województwie podkarpackim , w powiecie jarosławskim , w gminie Jarosław .
W latach 1975–1998 Tuczempy administracyjnie należały do województwa przemyskiego .
Geografia .
Charakterystyka geograficzna Tuczemp cechuje się pagórkowatym ukształtowaniem terenu , na wysokości ok. 206 m n.p.m .
Miejscowość położona jest w południowo-wschodniej części gminy Jarosław , przy drodze będącej wspólnym odcinkiem drogi krajowej nr 94 ( Zgorzelec – Korczowa ) i drogi krajowej nr 77 ( Lipnik – Przemyśl ) .
Pobliskimi miastami są : Jarosław i Radymno , oraz sąsiadujące wzdłuż dróg miejscowości : Munina i Ostrów .
Tuczempy podzielone były na kilka przysiółków , o nazwach charakterystycznych dla ich poszczególnego usytuowania i ukształtowania terenu .
