Tuczyn
Tuczyn ( ukr .
Тучин ) – wieś ( dawniej miasteczko ) na Ukrainie na terenie rejonu hoszczańskiego w obwodzie rówieńskim , nad rzeką Horyń .
Wieś liczy 2540 mieszkańców .
W II Rzeczypospolitej Tuczyn początkowo stanowił odrębną gminę miejską na prawach miasteczka w powiecie rówieńskim w woj .
