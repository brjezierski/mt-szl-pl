Tukums
Tukums ( dawniej niem .
`` Tuckum '' , pol .
`` Tukum '' ) – miasto w północno-zachodniej części Łotwy , na Wysoczyźnie Kurlandzkiej , na północ od drogi łączącej Rygę i Windawę , siedziba administracyjna okręgu Tukums .
W 2016 roku liczyło 18 334 mieszkańców .
Miasto charakteryzuje się w większości niską , drewnianą zabudową pochodzącą z pierwszej połowy XX wieku .
W mieście rozwinął się przemysł spożywczy , elektromaszynowy oraz meblarski .
Najstarszą częścią jest dzisiejsza ulica Talsi , która powstała nad rzeką , gdzie znajdował się kopiec zamkowy z drewnianym zamkiem .
Od 1253 Tukum był rządzony przez Zakon Kawalerów Mieczowych .
Pod koniec XIII wieku na brzegu rzeki Slocene zbudowano murowany zamek .
Zamek otaczała osada niemieckich kupców i rzemieślników .
Po rozpadzie Zakonu Kawalerów Mieczowych powstało nowe państwo , Księstwo Kurlandii i Semigalii .
Podczas II wojny światowej pod Tukum jesienią 1944 r. toczyły się ciężkie walki , kiedy jednostki Wehrmachtu walczyły o uwolnienie połączeń z Grupą Armii Północ na wschodzie .
