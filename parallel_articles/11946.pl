Śluzowoszczka podlaska
Śluzowoszczka podlaska ( `` Tulasnella eichleriana '' Bres . )
– gatunek grzybów z rodziny śluzowoszczkowatych ( `` Tulasnellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tulasnella , Tulasnellaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1903 r. Giacomo Bresàdola na drewnie cisa ( `` Taxus '' ) i sosny ( `` Pinus '' ) w Anglii .
W nazwie gatunkowej upamiętnił polskiego botanika Bogumira Eichlera .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Grzyb nadrzewny o rozpostartych owocnikach .
Charakteryzuje się strzępkami bez sprzążek i małymi , niemal kulistymi lub elipsoidalnymi zarodnikami , których średnica nigdy nie przekracza 3,5 μm szerokości .
Występowanie .
Podano występowanie śluzowoszczki podlaskiej głównie w Europie , poza nią na nielicznych miejscach w Ameryce Północnej i Azji .
W Europie znana jest w co najmniej 15 krajach .
W Polsce do 2003 r. W. Wojewoda przytoczył 5 stanowisk .
Według niego częstość występowania i stopień zagrożenia tego gatunku w Polsce nie są dokładnie znane .
Liczne nowe stanowiska tego gatunku podał Marcin Piątek w 2004 r. Według niego `` T. eichleriana '' jest najczęściej w Polsce występującym gatunkiem rodzaju `` Tulasnella '' .
Występuje w lasach mieszanych i buczynach na martwym drewnie drzew iglastych i liściastych .
W Polsce notowana na drewnie brzozy brodawkowatej , buka pospolitego i świerka pospolitego od czerwca do listopada .
