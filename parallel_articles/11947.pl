Śluzowoszczka międzyrzecka
Śluzowoszczka międzyrzecka ( `` Tulasnella pallida '' Bres . )
– gatunek grzybów z rodziny śluzowoszczkowatych ( `` Tulasnellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tulasnella , Tulasnellaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1903 r. Giacomo Bresàdola na podstawie przysłanych mu okazów zielnikowych zebranych przez Bogumira Eichlera w Polsce .
Synonim : `` Tulasnella albolilacea '' Bourdot & amp ; Galzin 1924 .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. Nazwę gatunkową utworzył od miejscowości Międzyrzec Podlaski , w której B. Eichler zbierał okazy grzybów i jako pierwszy podał wiele nowych ich stanowisk w Polsce .
Morfologia .
Grzyb nadrzewny o rozpostartych owocnikach .
Charakteryzuje się elipsoidalnymi , podłużnymi lub szeroko wrzecionowatymi bazydiosporami , które mają tendencję do zwężania się w obu końcach .
Szerokość zarodników zwykle przekracza 4 μm .
Występowanie .
Podano występowanie śluzowoszczki międzyrzeckiej głównie w Europie , poza nią we wschodniej części Kanady i na Uralu .
W Europie znana jest w co najmniej 10 krajach .
W Polsce do 2003 r. W. Wojewoda przytoczył 5 stanowisk .
Według niego częstość występowania i stopień zagrożenia tego gatunku w Polsce nie są dokładnie znane .
Trzy nowe stanowiska tego gatunku podał Marcin Piątek w 2004 r. Nowe stanowiska podaje także internetowy atlas grzybów .
Śluzowoszczka międzyrzecka zaliczona w nim jest do gatunków rzadkich .
Występuje w lasach mieszanych i liściastych na martwym drewnie .
W Polsce notowana głównie na drewnie dębów i wierzb .
