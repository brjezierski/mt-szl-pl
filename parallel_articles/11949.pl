Tulkarm ( muhafaza )
Tulkarm ( , `` Ţūlkarm '' ) – muhafaza Palestyny .
Leży w północno-zachodniej części Zachodniego Brzegu .
Od północnego wschodu sąsiaduje Dżaninem , od wschodu z Nablusem , a do południa z Kalkilją .
Od zachodu graniczy z izraelskimi dystryktami Centralnym i Hajfa .
Ma powierzchnię 246 km² .
Według danych Palestyńskiego Centralnego Biura Statystycznego na rok 2007 zamieszkało ją 158 213 osób , co stanowiło 4,2 % ludności Palestyny .
Znajdowało się tu wtedy 29 874 gospodarstw domowych .
Do 2015 roku liczba ludności wzrosła do 182 053 , a gęstość zaludnienia wynosiła 740 os./km² .
Jest to piąta najmniejsza pod względem liczby ludności muhafaza Palestyny .
