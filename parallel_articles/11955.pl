( 1070 ) Tunica
( 1070 ) Tunica – planetoida należąca do zewnętrznej części pasa głównego asteroid okrążająca Słońce w ciągu 5 lat i 296 dni w średniej odległości 3,23 au .
Została odkryta 1 września 1926 roku w Landessternwarte Heidelberg-Königstuhl w Heidelbergu przez Karla Reinmutha .
Nazwa planetoidy pochodzi od `` tunici '' popularnej nazwy goździcznika .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 1070 ) 1926 RB .
