Tupac Shakur
Tupac Amaru Shakur , również 2Pac i Makaveli ( ur .
16 czerwca 1971 w Nowym Jorku jako Lesane Parish Crooks , zm .
13 września 1996 w Las Vegas ) – amerykański raper , poeta , aktor i aktywista społeczny .
W oficjalnych dokumentach został zapisany jako Lesane Parish Crooks .
Dopiero gdy jego matka , Alice Faye Williams ( po ślubie Afeni Shakur ) , poślubiła Lumumbę Abdula Shakura , otrzymał nowe imię Tupac Amaru ( zob .
Tupac Amaru II ) .
Według Black Entertainment Television był najbardziej wpływowym raperem w historii , natomiast magazyn Rolling Stone umieścił go na 86. miejscu wśród 100 największych artystów wszech czasów .
