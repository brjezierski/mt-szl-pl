Tupac Shakur już jako dziecko przejawiał talent artystyczny .
Matka zapisała go do The 127th Street Ensemble , grupy teatralnej , gdzie jako 12-latek zadebiutował na scenie jako Travis w `` Rising in the Sun '' ( 1983 ) .
Trzy lata później , w czerwcu 1986 roku , Shakurowie przenieśli się do Baltimore , gdzie młody Tupac zapisał się do Baltimore School for the Performing Arts , rozpoczynając naukę baletu , aktorstwa i poezji .
Właśnie w Baltimore Shakur napisał swój pierwszy rapowy tekst i występował pod pseudonimem MC New York .
