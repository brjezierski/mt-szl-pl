Andrzej Grabowski ( prawnik )
Andrzej Grabowski – polski prawnik , doktor habilitowany nauk prawnych , profesor nadzwyczajny Uniwersytetu Jagiellońskiego , specjalista w zakresie teorii prawa .
Życiorys .
W 1988 ukończył studia prawnicze na Wydziale Prawa i Administracji Uniwersytetu Jagiellońskiego , a w 1992 studia w Europejskiej Akademii Teorii Prawa w Brukseli .
Na macierzystym wydziale UJ w 1995 na podstawie napisanej pod kierunkiem Tomasza Gizbert-Studnickiego rozprawy pt .
`` Sądowa argumentacja prawnicza ( podejście pragmatyczne ) '' otrzymał stopień naukowy doktora nauk prawnych w zakresie prawa , specjalność : teoria prawa .
Tam też uzyskał w 2010 stopień doktora habilitowanego nauk prawnych w zakresie prawa , specjalność : teoria i filozofia prawa .
W latach 2000–2001 był stypendystą Fundacji Aleksandra von Humboldta .
W 2017 został profesorem nadzwyczajnym w Katedrze Teorii Prawa Wydziału Prawa i Administracji Uniwersytetu Jagiellońskiego .
