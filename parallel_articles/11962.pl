Turcja
Turcja ( tur .
`` Türkiye '' ) , Republika Turcji ( tur .
`` Türkiye Cumhuriyeti '' ) – państwo położone w Azji Zachodniej na półwyspie Azja Mniejsza , a częściowo również w Europie Południowo-Wschodniej , ze stolicą w Ankarze .
Graniczy w Europie z Bułgarią i Grecją , natomiast w Azji z Syrią , Irakiem , Iranem , Azerbejdżanem , Armenią i Gruzją .
Część europejska – wschodnia Tracja – stanowi 3 % powierzchni i oddzielona jest od części azjatyckiej Morzem Marmara oraz cieśninami Bosfor i Dardanele .
Turcję od północy otacza Morze Czarne , od zachodu Morze Egejskie i Morze Marmara , a od południa Morze Śródziemne ( nazywane w języku tureckim Morzem Białym ) .
Republika Turcji została proklamowana w 1923 roku , poprzedzającym ją państwem było Imperium Osmańskie .
