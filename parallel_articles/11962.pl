Turcja leży na dwóch kontynentach : azjatyckim ( w 97 % powierzchni ) , w jego najdalej na zachód wysuniętej części , Azji Mniejszej ( w regionie zwanym także niegdyś Anatolią ) oraz europejskim ( w 3 % powierzchni ) , w regionie dawnej wschodniej Tracji na Półwyspie Bałkańskim .
Rozciągłość południkowa wynosi ok. 500 km , natomiast równoleżnikowa ok. 1600 km .
Kraj oblewa od północy Morze Czarne , od zachodu Morze Marmara i Morze Egejskie , a od południa Morze Śródziemne .
Przylądek Baba , najdalej wysunięty na zachód punkt Azji Mniejszej , jest jednocześnie najdalej wysuniętym na zachód punktem całej Azji .
