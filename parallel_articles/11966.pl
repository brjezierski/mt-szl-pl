Język turecki
Język turecki ( tur .
, IPA : [ ] , `` Türk dili '' ) – język należący do grupy oguzyjskiej języków tureckich , ojczysty dla ponad 83 mln ludzi na całym świecie , co czyni go najpowszechniejszym ze swojej rodziny językowej .
Turecki używany jest przede wszystkim w Turcji i na Cyprze Północnym , mniejsze skupiska mówiących znajdują się w Iraku , Grecji , Bułgarii , Macedonii Północnej , Kosowie , Albanii i w innych częściach Europy Wschodniej .
Turecki jest używany także przez wielu imigrantów w Europie Zachodniej , zwłaszcza w Niemczech .
Turecki dzieli się na wiele dialektów ; za podstawę języka literackiego i standard uważany jest turecki stambulski .
Korzeni języka należy doszukiwać się w Azji Środkowej , skąd wywodzą się pierwsze zapisy po turecku , datowane na prawie 1300 lat wstecz .
Na zachód zasięg języka osmańskotureckiego ( odmiany tureckiego używanej jako język administracji i literatury Imperium Osmańskiego ) rozszerzył się w momencie ekspansji terytorialnej kraju .
W 1928 , w wyniku jednej z reform Atatürka w czasach wczesnej Republiki Tureckiej , pismo osmańskie zastąpiono alfabetem łacińskim .
Obecnie standaryzację i dalsze reformy tureckiego prowadzi Instytut Języka Tureckiego .
Cechą wyróżniającą język turecki jest harmonia samogłoskowa i silna aglutynacyjność .
Podstawowy szyk zdania to podmiot–dopełnienie–orzeczenie .
Nie rozróżnia się klasy rzeczownikowej ani rodzaju gramatycznego .
Istnieje silne rozgraniczenie w tytułowaniu osób ( „ ty ” – „ wy ” ) oraz rozbudowany system wyrazów grzecznościowych .
Turecki używa zaimków w drugiej osobie zależnie od stopnia zażyłości , grzeczności , dystansu społecznego albo wieku .
Zaimek osobowy drugiej osoby liczby mnogiej i formy czasowników używane są także w odniesieniu do pojedynczej osoby celem okazania szacunku .
Okazjonalnie można także zastosować w liczbie mnogiej zaimek drugiej osoby liczby mnogiej ( `` sizler '' ) , aby okazać jeszcze większy dystans i szacunek .
Klasyfikacja .
Turecki jest członkiem oguzyjskiej rodziny językowej , podgrupy języków tureckich .
Pod wieloma względami pokrywa się z innymi językami oguzyjskimi , takimi jak azerski , turkmeński , kaszkajski , gagauski oraz bałkano-gagauski .
Rodzina języków tureckich zawiera około 30 języków używanych w Europie Wschodniej , Azji Środkowej i na Syberii .
Według niektórych naukowców jest to podgrupa rodziny ałtajskiej , do której zalicza się między innymi także język mongolski oraz koreański .
Około 40 % użytkowników języka tureckiego to użytkownicy rodzimi , dla których jest on językiem ojczystym .
Cechy charakterystyczne tureckiego , takie jak harmonia samogłoskowa , aglutynacyjność i brak rodzaju gramatycznego są cechami uniwersalnymi pośród języków tureckich i ałtajskich .
Historia .
Język turecki rozwijał się na terenie Anatolii od XI wieku , to jest od opanowania tych terenów przez plemiona tureckie pod wodzą Seldżuków .
Od XIX wieku trwał proces dostosowywania języka pisanego do mówionego , oczyszczania go z zawiłych konstrukcji oraz słów pochodzących z arabskiego i perskiego .
Poszukiwano przy tym słów rdzennie tureckich lub wykorzystywano zapożyczenia z języków zachodnioeuropejskich , między innymi z języka francuskiego .
Proces ów , zwany „ oczyszczaniem ” ( `` sadeleşme '' ) , przybrał na sile w latach 20. i 30 .
XX wieku , podczas rządów prezydenta Mustafy Kemala Atatürka .
Od 1928 język turecki zapisywany jest za pomocą alfabetu tureckiego , opartego na literach łacińskich .
Alfabet turecki ma 29 liter .
