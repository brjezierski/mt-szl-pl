Turek ( Białoruś )
Turek – dawna leśniczówka .
Tereny na których była położona , leżą obecnie na Białorusi , w obwodzie mińskim , w rejonie miadzielskim , w sielsowiecie Budsław .
Historia .
W latach 1921–1945 leśniczówka leżała w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie duniłowickim ( od 1926 w powiecie wilejskim ) , w gminie Budsław .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało wieś 11 osób , wszystkie były wyznania rzymskokatolickiego .
Jednocześnie 6 mieszkańców zadeklarowało polską przynależność narodową a 5 białoruską .
Były tu 2 budynki mieszkalne .
W 1931 w 2 domach zamieszkiwało 7 osób .
Miejscowość należała do parafii rzymskokatolickiej w Budsławiu .
Podlegała pod Sąd Grodzki w Krzywiczach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Budsławiu .
