Turkmenistan
Turkmenistan , Republika Turkmenistanu ( turkm .
`` Türkmenistan '' , `` Türkmenistan Respublikasy '' ; w okresie ZSRR funkcjonowała nazwa Turkmenia ) – państwo położone w Azji Środkowej , na północ od gór Kopet-Dag , pomiędzy Morzem Kaspijskim a Amu-darią .
Kraj rozciąga się na 1100 km od wschodu do zachodu oraz na 650 km z północy na południe .
Większość mieszkańców stanowią Turkmeni .
Turkmenistan graniczy z Afganistanem , Iranem , Uzbekistanem i Kazachstanem .
Warunki naturalne .
Około 80 % kraju leży na Nizinie Turańskiej .
Terytorium Turkmenistanu w większości pokrywa pustynia Kara-kum .
Mimo zabiegów zapobiegających pustynnieniu obserwuje się zwiększanie terenu pustyni .
W zależności od metodologii pomiaru pustynia zajmuje od 70 % do 80 % terytorium kraju .
Obszar pustyni jest praktycznie niezamieszkały , a tereny zasiedlone znajdują się głównie na przedgórzu Kopet-Dag w południowej części kraju oraz w dolinach rzecznych na południowym wschodzie .
Najwyższy szczyt to Ajribaba o wysokości 3139 m n.p.m .
Na terenie kraju jest kilka depresji , z których najgłębszą jest Akczakaja ( 81 m p.p.m .
) .
Klimat .
Turkmenistan ma ekstremalnie kontynentalny klimat .
Wyjątkami są obszary przybrzeżne Morza Kaspijskiego oraz góry .
Średnia roczna temperatura powietrza na północy kraju waha się od 12 do 17 °C i na południowym wschodzie od 15 do 18 °C .
Najchłodniejszym miesiącem jest styczeń , ze średnią temperaturą od -6 °C na północnym wschodzie do +4 °C w południowo-wschodniej części i +5 °C na krańcach zachodnich .
Średnia temperatura w najgorętszym miesiącu lipcu wynosi od 27 do 30 °C .
Najwyższe temperatury notowane są w środkowej i południowo-wschodniej części Kara-kum , gdzie wynoszą od 48 do 50 °C .
Najwyższe opady występują w górach i pogórzu , średnio do 398 mm , a najniższe w Kara Bogaz Goł , średnio 95 mm .
