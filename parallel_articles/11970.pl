Język turkmeński
Język turkmeński ( `` Тürkmençe '' , түркменче lub `` türkmen dili '' , түркмен дили ) – język z rodziny języków tureckich .
W Turkmenistanie posługuje się nim ok. 3,8 mln ludzi , poza tym ok. 3 mln w innych państwach : Afganistan ( 500 tys .
) , Iran ( 2 mln ) i Turcja ( 1 tys .
) .
Alfabet .
Oficjalnym systemem zapisu języka turkmeńskiego ( od 1991 r. ) jest specjalnie przystosowany alfabet łaciński , oparty na alfabecie tureckim .
Język turkmeński można także zapisywać w cyrylicy lub w alfabecie arabskim .
