Turku ( fiń .
`` Turku '' , szw .
`` Åbo '' , ros .
`` Турку '' ) – miasto w południowo-zachodniej Finlandii , u ujścia rzeki Aurajoki .
Stolica kraju do roku 1812 ( oficjalnie od 1809 ) , najstarsze miasto fińskie .
Liczy ponad 197 tys .
mieszkańców ( 2022 ) .
Uniwersytet założony w 1640 roku .
Obecnie stolica regionu Varsinais-Suomi , do 2009 roku prowincji Finlandia Zachodnia , ważny port ( w 2018 obsłużył 3,3 mln pasażerów ) .
Rozwinięty przemysł stoczniowy , metalowy , włókienniczy , spożywczy , maszynowy oraz hutniczy .
W skład zespołu miejskiego wchodzą : Raisio , Kaarina i Naantali .
