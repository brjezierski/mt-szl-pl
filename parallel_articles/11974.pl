Strategiczna gra turowa
Strategiczne gry turowe ( ang .
turn-based strategy , w skrócie TBS ) – rozgrywany w turach podgatunek komputerowych gier strategicznych .
Każdy gracz może w swojej turze przemieszczać swoje jednostki ( w zależności od określonych współczynników , np .
ilości punktów ruchu ) , rozgrywać bitwy czy ulepszać fortyfikacje .
Gry strategiczne rozgrywane w turach są najbliższe pierwowzorom wywodzącym się z planszowych gier strategicznych .
Podgatunkiem strategicznych gier turowych są taktyczne gry fabularne ( również odbywające się w turach ) łączące ten gatunek z RPG .
Strategie turowe w przeciwieństwie do strategicznych gier czasu rzeczywistego pozwalają graczowi na dłuższe zastanowienie się nad dalszymi posunięciami , przez co niekiedy mogą być zbliżone w swej naturze do szachów czy go .
