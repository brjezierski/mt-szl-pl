Turyn
Turyn ( wł .
`` Torino '' ) – miasto w północno-zachodnich Włoszech , przy ujściu rzeki Dora Riparia do rzeki Pad , na przedgórzu Alp Zachodnich .
Jest stolicą prowincji Piemont .
Patronem miasta jest św .
Jan Chrzciciel ( jego święto obchodzone 24 czerwca ) .
Turyn jest po Mediolanie drugim najważniejszym ośrodkiem gospodarczo-kulturalnym północnych Włoch .
Według danych na 2017 rok miasto zamieszkiwało 883 281 osób , 6785,6 os./km² , aglomeracja liczy 2,2 mln mieszkańców .
