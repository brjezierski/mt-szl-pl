Andrzej Lepper
( ur .
13 czerwca 1954 w Stowięcinie , zm .
5 sierpnia 2011 w Warszawie ) – polski polityk , rolnik i związkowiec , założyciel i pierwszy przewodniczący partii Samoobrona Rzeczpospolitej Polskiej i Związku Zawodowego Rolnictwa „ Samoobrona ” .
Poseł na Sejm IV i V kadencji w latach 2001–2007 , wicemarszałek Sejmu IV i V kadencji .
Poseł do Parlamentu Europejskiego V kadencji ( 2004 ) .
Wiceprezes Rady Ministrów oraz minister rolnictwa i rozwoju wsi w rządzie Kazimierza Marcinkiewicza , a następnie w rządzie Jarosława Kaczyńskiego ( od 5 maja 2006 do 22 września 2006 oraz od 16 października 2006 do 9 lipca 2007 ) .
Kandydat w wyborach prezydenckich w 1995 , 2000 , 2005 i 2010 .
Życiorys .
Wykształcenie i praca zawodowa .
W 1974 ukończył Państwowe Technikum Rolnicze w Sypniewie jako abiturient , jednak nie przystąpił do egzaminu dojrzałości .
W latach 1974–1976 odbył zasadniczą służbę wojskową .
W latach 1976–1977 pracował w Stacji Hodowli Roślin w Gorzynie i Państwowym Gospodarstwie Rolnym w Rzechcinie .
Od 1977 do 1980 był kierownikiem gospodarstwa w Kusicach , należącego do Państwowego Ośrodka Hodowli Zarodowej w tej miejscowości .
Został wówczas najmłodszym kierownikiem PGR w Polsce .
