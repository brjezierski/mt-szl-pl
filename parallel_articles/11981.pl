Turniej pretendentów 2022
Turniej pretendentów 2022 – turniej szachowy organizowany przez Międzynarodową Federację Szachową ( FIDE ) w celu wyłonienia pretendenta do tytułu Mistrza Świata w szachach klasycznych 2023 .
Odbył się on między 16 czerwca a 5 lipca 2022 w Madrycie .
Zwyciężył go Jan Niepomniaszczij , który jako jeden z pięciu szachistów w historii , wygrał dwa turnieje pretendentów z rzędu .
Zagra on o mistrzostwo świata z Dingiem Lirenem , drugim zawodnikiem turnieju , po rezygnacji Magnusa Carlsena z obrony tytułu .
Zawodnicy .
W turnieju pretendentów 2022 bierze udział 8 zawodników .
Format .
Turniej pretendentów 2022 był rozgrywany systemem dwukołowym na dystansie 14 partii .
Początkowo zwycięzca turnieju miał rozegrać mecz o mistrzostwo świata w szachach klasycznych z Magnusem Carlsenem w 2023 .
20 lipca 2022 Carlsen ogłosił , że rezygnuje z obrony tytułu .
W tym wypadku w meczu o mistrzostwo świata zmierzy się dwóch najlepszych zawodników z turnieju pretendentów .
Turniej rozgrywany był w tempie 120 minut dla zawodnika na wykonanie pierwszych 40 posunięć w partii , następnie z dodatkowymi 60 minutami na kolejne 20 ruchów , a później jeszcze z dalszymi 15 minutami na dokończenie partii .
Dodatkowo zawodnicy od 61. ruchu otrzymywali po 30 sekund za każdy ruch .
Zawodnicy nie mogli oferować remisów przed 40. ruchem .
Dogrywki .
W przypadku remisu na pierwszym miejscu po czternastu rundach następowała dogrywka w następującym formacie :
W przypadku , jeśli dwóch lub więcej graczy miało taką samą ilość punktów na innych miejscach niż pierwsze , ich miejsca ustalane były przez punktację pomocniczą składającą się z systemu Sonnenborna-Bergera , ilości wygranych partii i rezultatów partii między graczami z tą samą ilością punktów .
Nagrody finansowe .
Pula nagród turnieju pretendentów 2022 wynosiła 500 000 euro .
Zwycięzca turnieju otrzymał 48 000€ , zdobywca drugiego miejsca 36 000€ , a zdobywca trzeciego miejsca 24 000€ .
Za każde zdobyte pół punktu zawodnicy otrzymywali dodatkowo 3 500€ .
Harmonogram .
Gracze reprezentujący te same państwa rozgrywali między sobą partie w rundzie 1. i 8 .
