Smalec
Smalec – tłuszcz zwierzęcy wykorzystywany w sztuce kulinarnej do przyrządzania potraw , jak również bezpośredniego spożywania .
Jak powstaje .
Jest wytapiany najczęściej ze słoniny wieprzowej oraz podgardla bądź boczku .
Przeznaczony do bezpośredniego spożycia często wzbogacany jest o odrobinę soli , kawałki rozdrobnionej kiełbasy i cebuli lub skwarki , czasami też inne , mniej konwencjonalne dodatki , jak np .
kawałki drobno pokrojonego jabłka .
Skład chemiczny .
Głównym składnikiem smalcu ( ponad 99 % ) są tłuszcze , wśród których przeważają te , które zawierają kwasy nienasycone ( ponad 55 % wagi ) .
Wbrew obiegowym opiniom ilość nasyconych kwasów tłuszczowych nie przekracza 40 % .
Udział nienasyconych kwasów tłuszczowych zależy od paszy , którą były karmione zwierzęta , z których wytopiono smalec oraz od sposobu wytapiania .
Oprócz tego smalec zawiera zwykle także niewielkie ilości cholesterolu oraz ślady soli mineralnych i fragmenty tkanek miękkich ( mięśni , skóry itp . )
zwierząt , z których został wytopiony .
Dotyczy to zwłaszcza smalcu ze skwarkami .
Dieta wysokotłuszczowa .
W 2001 roku stwierdzono , że tłuszcze nasycone ( w maśle , smalcu , śmietanie , wieprzowinie , kiełbasie , drobiu itp . )
nie szkodzą osobom z predyspozycjami do raka piersi po menopauzie .
13,5 % nasyconych kwasów tłuszczowych zawartych w smalcu to kwas stearynowy , który obniża poziom cholesterolu LDL .
Dla porównania , oliwa z oliwek zawiera ok. 2 % kwasu stearynowego .
Farmacja .
Smalec wieprzowy znalazł zastosowanie w farmacji jako surowiec farmaceutyczny w recepturze aptecznej pod nazwą łacińską `` Adeps suillus '' lub `` Axungia Porci '' .
Smalec zastosowany jako podłoże do wytwarzania maści leczniczych musi odpowiadać wymaganiom jakości farmaceutycznej określanym w aktualnej monografii Farmakopei Polskiej .
Jest podłożem `` ( vehiculum ) '' dla maści siarkowej , maści Wilkinsona , maści z jodkiem potasowym .
