Tłusty czwartek
Tłusty czwartek – ostatni czwartek przed wielkim postem ( czyli 52 dni przed Wielkanocą ) w kalendarzu chrześcijańskim .
Znany także jako zapusty , mięsopust , karnawał .
Tłusty czwartek rozpoczyna ostatni tydzień karnawału .
W Polsce oraz w katolickiej części Niemiec , wedle tradycji , w tym dniu dozwolone jest objadanie się .
Data .
Ponieważ data tłustego czwartku zależy od daty Wielkanocy , dzień ten jest świętem ruchomym .
Następny czwartek jest czwartkiem po Środzie Popielcowej i należy do okresu wielkiego postu , podczas którego chrześcijanie ze względów religijnych ( pokutnych ) , jako przygotowanie do Świąt Wielkanocy powinni zachowywać post , czyli wstrzemięźliwość od przyjmowania niektórych pokarmów .
Może wypaść pomiędzy 29 stycznia a 4 marca .
Najpopularniejsze potrawy to pączki i faworki , zwane również w niektórych regionach chrustem lub chruścikami .
Dawniej objadano się pączkami nadziewanymi słoniną , boczkiem i mięsem , które obficie zapijano wódką .
Staropolskie przysłowie mówi : `` Powiedział Bartek , że dziś tłusty czwartek , a Bartkowa uwierzyła , dobrych pączków nasmażyła '' .
