Tuvalu
Tuvalu lub Tuwalu ( dawniej : `` Terytorium Tuvalu '' , `` Wyspy Ellice '' ) – państwo położone na Oceanie Spokojnym w zachodniej Polinezji ( na północ od Fidżi ) , Tuvalu tworzy archipelag po angielsku zwany `` Tuvalu Islands '' ( dawne nazwy angielskie to Lagoon Islands i Ellice Islands ) , po polsku zaś `` Wyspami Lagunowymi '' .
Historia .
Wyspy Ellice zostały zasiedlone w V wieku n.e .
Po raz pierwszy odkryte przez Europejczyków w 1568 ( Hiszpan Alvaro Mendana de Neyra ) .
Odkryte ponownie w 1764 przez Brytyjczyka Johna Byrona .
Od 1877 pod zarządem Brytyjskiej Wysokiej Komisji Zachodniego Pacyfiku .
Od 1892 stanowiły najpierw część protektoratu , a potem od 1916 część kolonii o nazwie Wyspy Gilberta i Lagunowe .
W latach 1942–1943 okupowane przez Japonię , potem zajęte przez wojska USA .
W 1975 Wyspy Ellice wydzieliły się jako samodzielna kolonia ( po referendum w 1974 ) , a 3 lata później ( 1 października 1978 ) ogłosiły swą niezależność od Wielkiej Brytanii i zmieniły nazwę na Tuvalu .
Tuvalu stało się członkiem Brytyjskiej Wspólnoty Narodów ( `` Commonwealth '' ) .
Od 2000 członek ONZ , w 2008 po raz pierwszy uczestniczyło w igrzyskach olimpijskich .
Geografia .
Tuvalu obejmuje 6 atoli i 3 wyspy , tworzących archipelag Wysp Lagunowych .
Geograficznie należy do Polinezji .
Atole : Vaitupu ( 5,60 km² ) , Nanumea ( pow .
3,87 km² ) , Nukufetau ( 2,99 km² ) , Nui ( 2,83 km² ) , Funafuti ( 2,79 km² ) i Nukulaelae ( 1,82 km² ) mają lagunę połączoną z oceanem , wyspy Nanumanga ( 2,78 km² ) i Niutao ( 2,53 km² ) mają lagunę niepołączoną z oceanem , natomiast Niulakita ( 0,42 km² ) nie ma laguny .
Łączna powierzchnia wynosi 25,63 km² , a największą wyspą jest Vaitupu ( 4,9 km² ) .
Archipelag ciągnie się na odcinku 600 km z północnego zachodu na południowy wschód .
