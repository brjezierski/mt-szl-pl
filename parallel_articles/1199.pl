Andrzej Roczniok
Andrzej Roczniok ( ur .
29 października 1958 w Zabrzu ) – działacz opozycyjny w PRL , od 1980 członek „ Solidarności ” .
Samorządowiec , działacz na rzecz uznania narodowości śląskiej .
Życiorys .
W 1979 ukończył Liceum Ogólnokształcące nr 1 im .
Włodzimierza Ilicza Lenina w Zabrzu .
Pracował następnie magazynowo , a od 1980 do 1991 jako listonosz .
W okresie 1980–1981 przewodniczył zabrzańskiemu kołu Konfederacji Polski Niepodległej .
W latach 1981–1982 internowany .
Kolporter wydawnictw niezależnych .
W latach 1989–1992 działał w Stowarzyszeniu Chrześcijańsko-Demokratycznym , którego był założycielem .
W kadencji 1990–1994 zasiadał w Radzie Miejskiej Zabrza z ramienia Bloku Chrześcijańsko-Demokratycznego , był także członkiem zarządu miasta .
W latach 1993–2005 pozostawał członkiem Zarządu Śląskiego Unii Polityki Realnej , w 2005 wystąpił z partii .
Były działacz Ruchu Autonomii Śląska ( do 2007 ; od 2005 zasiadał w radzie naczelnej ) .
Od 2002 przewodniczący niezarejestrowanego Związku Ludności Narodowości Śląskiej – organizacji śląskiej mniejszości narodowej .
Podczas spisu powszechnego w 2002 apelował o deklarowanie narodowości śląskiej i koordynował deklarowanie narodowości śląskiej ze strony RAŚ .
Doprowadził do nadania w dniu 18 lipca 2007 językowi śląskiemu kodu ISO 639-3 : „ szl ” .
W lipcu 2007 zainicjował obchody 1144 rocznicy Chrztu Śląska .
Jako przedstawiciel języka śląskiego wziął udział w powołaniu w dniu 13 grudnia 2003 w Gdańsku organizacji PolBLUL ( Unia Języków Regionalnych i Mniejszościowych w Polsce ) – odnogi EBLUL ( Europejskie Biuro Języków Rzadziej Używanych ) .
W styczniu 2005 skutecznie wsparł ratowanie polskich mszy w Essen w Niemczech .
Twierdzi , że język śląski jest uznawany przez Bibliotekę Kongresu USA .
W 2017 został jednym z założycieli partii Ślonzoki Razem ( zarejestrowanej w 2018 ) .
Doprowadził do przypomnienia informacji , że po styczniu 1945 na terenie Oświęcimia istniały co najmniej 4 obozy koncentracyjne dla Ślązaków ( 2 polskie i 2 sowieckie ) i że również na terenie Brzezinki istniały po wojnie obozy koncentracyjne .
Zainicjował i współorganizował konferencję „ Śląsko godka – jeszcze gwara czy jednak już język ? ” , która odbyła się 30 czerwca 2008 w Sali Sejmu Śląskiego w Katowicach .
28 sierpnia 2008 wraz z Rudolfem Kołodziejczykiem w imieniu ZLNS zwrócił się z apelem do premiera Donalda Tuska o szybkie przez Polskę uznanie niepodległości Abchazji i Osetii Północnej .
W sierpniu 2009 w oświadczeniu ZLNŚ nazwał deklarację o nieagresji , którą w 1934 Józef Piłsudski podpisał z Niemcami paktem Hitler-Piłsudski .
W wyborach samorządowych w 2010 bez powodzenia usiłował zarejestrować komitet wyborczy „ Narodowość Śląska a inksze Ślonzoki ” , po czym zakwestionował tryb odmowy rejestracji , składając protest w Sądzie Okręgowym w Gliwicach i domagając się unieważnienia wyborów do Sejmiku Województwa Śląskiego .
Wydawca „ Kuriera Chadeckiego ” i „ Slōnsko Nacyja ” .
Prowadzi wydawnictwo ( ) .
Zgodnie z dokumentacją Instytutu Pamięci Narodowej , Andrzej Roczniok był w latach 1978–1981 tajnym współpracownikiem służb bezpieczeństwa PRL o pseudonimie „ Arocszebla ” .
