Tuwińcy
Tuwińcy ( dawniej `` Urianchajcy '' , `` Sojoci '' , czasem nazywani `` Tuwijczykami '' ) – naród turkijski , żyjący w Tuwińskiej Republice Autonomicznej w południowej Syberii , w Mongolii ( 24 tys . )
i Chinach .
Społeczność Tuwińców w Rosji liczy 264 tysiące osób ( w 2010 ) .
Mongolscy Tuwińcy zasymilowali się wśród Mongołów i nie używają już własnego języka tuwińskiego .
Tuwińcy dzielą się na dwie grupy .
Wschodni Tuwińcy , nazywani Todżyńcami , stanowią około 5 % społeczności , od reszty Tuwińców odróżnia ich tradycyjny sposób gospodarowania : są myśliwymi i hodowcami reniferów .
Zajęciem reszty Tuwińców była hodowla bydła .
Obecnie prowadzą półosiadły tryb życia , mieszkają , podobnie jak Mongołowie , w jurtach .
Pierwotną religią Tuwińców był szamanizm , współcześnie praktykowany przez nielicznych .
Od XVIII wieku zdecydowana większość ludności tuwińskiej wyznaje buddyzm tybetański .
Do 1911 kontrolę nad ziemiami Tuwińców sprawowały Chiny , w okresie 1911–1914 faktycznie niepodległa Mongolia , a od kwietnia 1914 Tuwa była częścią Rosji .
