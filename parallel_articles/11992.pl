Historia .
Miasto wzmiankowane pierwszy raz w 1146 .
Początkowo znajdowało się w Księstwie Riazańskim .
W 1503 zostało włączone do Wielkiego Księstwa Moskiewskiego .
W latach 1507–1520 wzniesiono kreml w celu obrony przed Tatarami krymskimi .
W XVI i XVII wieku było ośrodkiem hutnictwa .
W 1712 z rozkazu Piotra I Wielkiego wybudowano w Tule pierwszą w Rosji fabrykę broni .
W 1775 zostało stolicą guberni .
W XIX wieku było ośrodkiem jubilerstwa , handlu wyrobami srebrnymi oraz produkcji samowarów .
