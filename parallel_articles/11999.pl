Tworki ( przystanek kolejowy )
Tworki – przystanek Warszawskiej Kolei Dojazdowej położony przy ul .
Natolińskiej i Krętej w dzielnicy Tworki w Pruszkowie .
W roku 2017 przystanek obsługiwał 300–499 pasażerów na dobę .
Opis przystanku .
Peron .
Przystanek składa się z dwóch peronów bocznych .
Każdy peron posiada jedną krawędź peronową .
Na peronie znajdują się dwie blaszane wiaty przystankowe .
Punkty sprzedaży biletów .
Na przystanku pełnozakresowa obsługa biletowa obsługiwana jest wyłącznie przez biletomat na peronie drugim .
Przejazd kolejowy .
Na wschodniej głowicy peronu drugiego znajduje się przejazd kolejowy , prowadzący na teren Szpitala Tworkowskiego .
Położony jest wzdłuż ul .
Natolińskiej .
