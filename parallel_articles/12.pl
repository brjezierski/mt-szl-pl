.as – krajowa domena internetowa najwyższego poziomu przypisana dla stron internetowych z wysp Samoa Amerykańskie , jest aktywna od 1997 roku i administrowana przez AS Domain Registry .
Informacje dodatkowe .
Domena .as jest często wykorzystywana przez podmioty spoza Samoa Amerykańskiego .
`` AS '' oraz `` A/S '' często występuje w nazwach firm z Norwegii i Danii .
W związku z tym firmy z tych krajów często korzystają z tej domeny .
`` AS '' jest również angielskim słowem i jest często wykorzystywana do tworzenia ciekawych adresów , takich jak np .
se.as ( – morza ) .
