Andrzej Stania
Andrzej Stania ( ur .
3 lutego 1948 w Rudzie ) – polski samorządowiec , od 2000 do 2010 prezydent Rudy Śląskiej , prezes Związku Górnośląskiego .
Życiorys .
Z zawodu górnik , do czasu przejścia na emeryturę pracował pod ziemią , następnie w dozorze , dochodząc do stanowiska głównego inżyniera ds .
BHP .
Od 17 kwietnia 2000 do 24 listopada 2000 był przewodniczącym rady miejskiej w Rudzie Śląskiej .
24 listopada 2000 został powołany na urząd prezydenta miasta .
W 2002 wygrał wybory bezpośrednie na to stanowisko .
W wyborach w 2006 z powodzeniem ubiegał się o reelekcję , otrzymując w pierwszej turze prawie 75 % głosów .
W 2010 przegrał w drugiej turze z Grażyną Dziedzic , wchodząc w skład rady miasta , do której był wybierany również w 2014 i 2018 .
Zaangażował się w działalność partyjną , wstępując do Platformy Obywatelskiej .
Został członkiem Związku Górnośląskiego , w latach 1992–1997 był wiceprezesem zarządu tej organizacji , a w latach 2010–2014 pełnił funkcję jego prezesa .
Żonaty ( żona Waleria ) , ma czworo dzieci .
W 2009 otrzymał Złoty Krzyż Zasługi .
