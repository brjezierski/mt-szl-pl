Tyberiada
Tyberiada ( , ; , `` Ṭabariyyah '' ; ) – miasto położone w Dystrykcie Północnym w Izraelu .
Według danych z 2010 Tyberiada miała 41 300 mieszkańców .
Tyberiada jest lokalnym ośrodkiem usługowo-przemysłowym , a także węzłem komunikacyjnym , w którym zbiegają się dwie drogi krajowe .
Miasto stanowi centrum administracyjne regionu .
Jest to znane uzdrowisko i ośrodek turystyczny położony na zachodnim brzegu Jeziora Tyberiadzkiego , w Dolnej Galilei .
Jest to najniżej położone miasto w Izraelu .
Tyberiada jest jednym z czterech świętych miast judaizmu – obok Jerozolimy , Safedu i Hebronu .
Położenie .
Tyberiada ( przy współrzędnych ) leży w środkowej części Dystryktu Północnego .
Miasto położone jest we wschodniej części Dolnej Galilei na północy Izraela .
Leży na północy depresji Rowu Jordanu , na wysokości 200 metrów p.p.m .
na zachodnim brzegu Jeziora Tyberiadzkiego , i jest oddalone około 42 kilometry od Morza Śródziemnego .
Powierzchnia miasta wynosi 10,671 km² .
Rozciągłość zabudowań miejskich w kierunku południkowym wynosi ok. 3,4 km , a w kierunku równoleżnikowym ok. 3,3 km .
