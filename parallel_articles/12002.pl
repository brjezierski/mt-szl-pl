Tybetka
Tybetka – nazwa elementu odzieży wykonanego z cienkiej tkaniny z wełny czesankowej owiec lub kóz tybetańskich ( tzw .
tybetu ) , drukowanej w motywy kwiatowo-roślinne , głównie zróżnicowane układy stylizowanych kwiatów .
Zastosowanie .
Spódnice
Tybet , wprowadzony na przełomie XIX i XX wieku , przyjął się w ludowym stroju góralskim jako typowy materiał na spódnice .
