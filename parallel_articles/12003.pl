Tybory-Żochy
Tybory-Żochy – wieś w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
Zaścianek szlachecki Żochy należący do okolicy zaściankowej Twarogi położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Michała Archanioła w Jabłonce Kościelnej .
