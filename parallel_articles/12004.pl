Tychy ( stacja kolejowa )
Tychy – stacja kolejowa w Tychach , w województwie śląskim , w Polsce .
Posiada według klasyfikacji PKP kategorię C .
Opis .
Stację obsługują połączenia Szybkiej Kolei Regionalnej , a także połączenia regionalne do większych miast , miejscowości turystycznych i uzdrowiskowych .
Przylegający do stacji kolejowej wielostanowiskowy dworzec autobusowy obsługują trolejbusy i autobusy ZTM ( w tym linia lotniskowa `` AP4 '' ) oraz autobusy dalekobieżne ( w tym międzynarodowe ) .
W latach 2010–2015 wyremontowano halę dworcową , wybudowano przy dworcu nowy dworzec autobusowy oraz wybudowano wielopoziomowy parking P & amp ; R. Miasto Tychy dzierżawi budynek dworca do 2039 .
