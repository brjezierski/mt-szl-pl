Tygodnik
Tygodnik – czasopismo ukazujące się raz w tygodniu .
W polskim nazewnictwie gatunkowym periodyków nazwa ta , utworzona na wzór dziennika pojawiła się na początku XIX wieku ( Tygodnik Wileński 1804 ) , zastępując dawniejszą nazwę `` pismo tygodniowe '' , powstałą pod wpływem niemieckim .
Określenie to często stanowi część składową tytułu .
Część tygodników ukazuje się w ostatnie dni przed weekendem i zawiera program telewizyjny .
Większość opisuje wydarzenia społeczne , polityczne , gospodarcze , ekonomiczne i kulturalne z kraju i zagranicy .
Niektóre tygodniki mają wyraźne profile światopoglądowe , najczęściej odnoszące się do światopoglądu politycznego , np .
lewicowe , liberalne czy konserwatywne .
Istnieją tygodniki regionalne i miejskie , zajmujące się życiem społecznym i kulturalnym mniejszych społeczności .
