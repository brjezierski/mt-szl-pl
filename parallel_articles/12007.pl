Historia .
Siedmiodniowy tydzień ma swoje początki w starożytnej Mezopotamii ok. 2000 lat p.n.e .
Ówcześni astrologowie zauważyli , że poza tak zwanymi gwiazdami stałymi na niebie gołym okiem można wyróżnić siedem ruchomych ( „ błądzących ” ) ciał niebieskich .
Były to Słońce , Księżyc , Mars , Wenus , Jowisz , Merkury oraz Saturn .
Siedem stało się liczbą świętą , a każdy dzień otrzymał za patrona jedno z tych ciał niebieskich .
Tydzień został wprowadzony do urzędowego kalendarza Cesarstwa Rzymskiego w 321 roku n.e .
Średniowieczny kalendarz kościelny jest oparty na urzędowym tygodniu Cesarstwa Rzymskiego .
