Tiabendazol
Tiabendazol , TBZ – organiczny związek chemiczny , pochodna benzimidazolu i tiazolu .
Wykorzystywany do ochrony owoców przed grzybami ( fungicyd ) i pasożytami .
Stosuje się go jedynie powierzchniowo , najczęściej przez spryskanie albo zanurzenie owoców w jego wodnym roztworze .
Używa się go do bananów , pomarańczy i innych owoców .
Na liście E ma numer E233 .
Dopuszczalna dobowa dawka dla człowieka wynosi 0,1 mg na kilogram masy ciała .
Przy jej nieprzekraczaniu związek nie wykazuje żadnego szkodliwego wpływu na organizm , ulega hydrolizie w wątrobie i jest wydalany przez nerki .
