Andrzej Wajda
Andrzej Witold Wajda ( ur .
6 marca 1926 w Suwałkach , zm .
9 października 2016 w Warszawie ) – polski reżyser filmowy i teatralny , w młodości aktywny jako malarz .
W sezonie 1989/1990 dyrektor artystyczny Teatru Powszechnego w Warszawie .
Czterokrotnie jego filmy były nominowane do Oscara dla najlepszego filmu nieanglojęzycznego .
W latach 1989–1991 senator I kadencji .
Kawaler Orderu Orła Białego .
Popularność przyniosły mu filmy inicjujące tzw .
polską szkołę filmową : `` Kanał '' oraz `` Popiół i diament '' , w których dokonywał rozrachunku z czasami II wojny światowej .
Dokonał ekranizacji wielu dzieł literackich , jak `` Popioły '' , `` Brzezina '' , `` Wesele '' , `` Ziemia obiecana '' , `` Panny z Wilka '' i `` Pan Tadeusz '' .
Współtworzył kino moralnego niepokoju , w którego ramach powstały osadzone w tematyce społecznej , demaskujące patologie systemu komunistycznego w Polsce filmy : `` Człowiek z marmuru '' i jego kontynuacja `` Człowiek z żelaza '' , która została nagrodzona Złotą Palmą na 34 .
MFF w Cannes .
Do ostatnich dokonań artysty należą : martyrologiczny film `` Katyń '' oraz eksperymentalny `` Tatarak '' .
Reżyser zainicjował również powstanie istniejącego w latach 1972–1983 Zespołu Filmowego „ X ” , Mistrzowskiej Szkoły Reżyserii Filmowej utworzonej w 2002 roku oraz autorskiego studia filmowego .
Jego działalność teatralna obejmowała wystawianie sztuk w teatrach gdańskich , warszawskich , krakowskich ( `` Z biegiem lat , z biegiem dni… '' ) oraz za granicą .
Jego twórczość nawiązywała do polskiego symbolizmu i romantyzmu , była próbą rozrachunku z mitami polskiej świadomości narodowej .
Za zasługi dla rozwoju kinematografii był wielokrotnie nagradzany , między innymi w 2000 roku otrzymał Nagrodę Akademii Filmowej ( Oscara ) za całokształt twórczości .
