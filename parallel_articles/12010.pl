Teatr
Teatr – rodzaj sztuki widowiskowej , w której aktor lub grupa aktorów na żywo przedstawia sztukę dla zgromadzonej publiczności .
Terminem teatr określa się też sam spektakl teatralny lub budynek , w którym odgrywane jest przedstawienie .
Elementy struktury spektaklu .
Podstawą istnienia teatru jest aktor , który buduje dla widza umowną rzeczywistość teatralną .
Spektakl bez aktora nie jest możliwy .
Aktor buduje swoją kreację sceniczną za pomocą ruchu scenicznego ( niekiedy jest to szczegółowo opracowana choreografia ) , gestu i mimiki .
Teatr to ukierunkowane działania aktorów na użytek publiczności , która jest drugim niezbędnym elementem każdego spektaklu .
Pełnowartościowy spektakl teatralny to taki , w którym następuje swego rodzaju porozumienie pomiędzy sceną a widownią .
