Tyler ( rower )
Tyler – model małego roweru składanego produkowanego w Polsce .
Długość 140 cm , wysokość 70 cm , średnica koła 20 cali .
Pod nazwą tą produkowano też rowery nieskładane typu kolarskiego o kole 20 '' z długim siodłem z podpórką biegnącą od tylnej osi oraz szeroką kierownicą , zawiniętą jak w rowerach kolarskich .
Tyler na tylnej osi był wyposażony w 5-przekładniową przerzutkę marki Romet .
