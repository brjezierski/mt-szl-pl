Tympanella galanthina
Tympanella galanthina ( Cooke & amp ; Massee ) E. Horak – gatunek grzybów z rodziny gnojankowatych ( `` Bolbitiaceae '' ) , należący do monotypowego rodzaju Tympanella E. Horak .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tympanella , Bolbitiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1890 r. Cooke i Massee nadając mu nazwę `` Agaricus galanthinus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1971 r. Egon Horak , przenosząc go do rodzaju `` Tympanella '' .
Synonimy nazwy naukowej :
Charakterystyka .
Grzyby rozwijające się w glebie , wytwarzające naziemne owocniki o białawych , dzwonkowatych ( z tępym szczytem i podwiniętym brzegiem ) kapeluszach .
Miąższ kapeluszy jest cienki i mięsisty , a ich powierzchnia gładka , hymenofor blaszkowy .
Trzony długości ok. 7,6 cm , gładkie , cienkie , puste w środku , cylindryczne .
Zarodniki eliptyczne , o wymiarach 12×6 μm .
Gatunek ten występuje w Nowej Zelandii .
