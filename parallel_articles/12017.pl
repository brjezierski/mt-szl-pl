Tenis
Tenis , tenis ziemny – dyscyplina sportowa rozgrywana na korcie tenisowym , polegająca na przebijaniu rakietą tenisową piłki ponad lub obok siatki na pole przeciwnika , celem zdobycia punktu bezpośrednio lub pośrednio , w wyniku błędu przeciwnika .
Może być rozgrywana pojedynczo ( tzw .
`` singiel '' ) lub w dwuosobowych zespołach zawodników jednej płci ( `` debel '' ) lub obu ( `` mikst '' ) .
Tenis należał do grona letnich dyscyplin olimpijskich w latach 1896–1924 .
Do ich programu powrócił w 1988 .
Wstęp .
Współczesna wersja tenisa narodziła się w Wielkiej Brytanii pod koniec XIX w. jako `` lawn tennis '' ( pol .
`` tenis na trawie '' ) .
Wkrótce po swoim powstaniu tenis rozprzestrzenił się wśród wyższych warstw anglojęzycznych społeczeństw zanim stał się popularny na całym świecie .
Tenis jest sportem olimpijskim uprawianym na wszystkich kontynentach przez ludzi w różnym wieku .
W tenisa może grać każdy , kto jest w stanie utrzymać w ręku rakietę tenisową , w tym osoby poruszające się na wózkach inwalidzkich , zawodnicy głusi i niewidomi .
Reguły tenisowe , które po raz pierwszy spisano w 1877 , w następnych dziesięcioleciach uległy jedynie kilku poważniejszym zmianom .
Jedną z nich jest , wprowadzona w latach 70 .
XX wieku , rozgrywka tie-breakowa , drugą natomiast zaaprobowana na początku XXI w. technologia `` Hawk-Eye '' ( pol .
`` Jastrzębie Oko '' ) , umożliwiająca weryfikację decyzji sędziego w przypadku , gdy piłka trafia w pobliże którejkolwiek z linii kortu .
Najważniejszymi rozgrywkami tenisowymi w roku są cztery turnieje tworzące tzw .
`` Wielki Szlem '' : Australian Open , Roland Garros , Wimbledon oraz US Open .
Wśród zawodów reprezentacji międzynarodowych wyróżnia się : Puchar Davisa , Hopmana , rozgrywki Fed Cupu oraz Drużynowy Puchar Świata .
Historia .
Początki .
Gry przypominające tenis znane są z czasów starożytnych .
Na historycznych reliefach widoczne są niewiasty w tunikach i mężczyźni w chitonach podbijający piłeczkę .
W Rzymie uprawiano „ trigon ” – zawodnicy ciężkimi pałkami odbijali piłki wypełnione ziarnami figi , zaś w łaźniach znajdowały się specjalne miejsca do gry w piłkę .
Homer zaś w „ Odysei ” opisał Nauzykę odbijającą piłkę .
We Włoszech w połowie XVI wieku znana była gra w „ pallone ” o systemie liczenia punktów podobnym do tenisa .
Najazdy Saracenów w VII wieku przyniosły do Francji zabawę polegają na odbijaniu piłki ręką .
Inną średniowieczną uprawianą przez mnichów grą w europejskich klasztorach było uderzanie piłki dłonią .
Gry te stanowiły zaczątek do powstania protoplasty tenisa – `` jeu de paume '' ( pol .
`` gra dłonią '' ) .
Grę tę zaczęto określać mianem `` prawdziwego '' lub `` królewskiego '' tenisa .
Wkrótce potem wprowadzono do użytku skórzane rękawice , te natomiast zastąpiły w XVI w. prototypy pierwszych rakiet , najpierw w postaci pokrytej pergaminem łopatki , a następnie drewnianej paletki .
Pierwsze piłki do tenisa były wykonane z drewna .
Z czasem ustąpiły one pola lepiej odbijającym się skórzanym piłkom wypełnionym celulozą Gra zyskiwała na popularności , zwłaszcza we Francji , gdzie spopularyzowali ją członkowie rodziny królewskiej , m.in .
Ludwik XIII , Henryk II czy Karol IX , oraz w Anglii , której królowie Henryk VII oraz Henryk VIII byli zapalonymi graczami i rozkazali budować korty w całym kraju .
Według legendy to właśnie Henryk VIII „ wymyślił ” serwis ( ang .
`` service '' ) , czyli uderzenie rozpoczynające wymianę – ponieważ król był zbyt gruby , by móc podrzucić piłkę do góry robili to za niego służący ( ang .
`` servants '' ) .
Narodziny współczesnej gry .
Tenis w obecnej postaci może być wywodzony z dwóch różnych źródeł .
Między 1859 a 1865 major Harry Gem wraz ze swoim przyjacielem Augurio Parerą opracowali grę będącą mieszanką sportów rakietowych i baskijskiej gry `` peloty '' .
Pierwsze mecze tenisowe rozgrywali na trawniku do gry w krokieta w posiadłości Gema w Birmingham .
W 1872 razem z dwoma miejscowymi lekarzami , Fredericiem Haynesem i Arthurem Tomkinem założyli pierwszy na świecie klub tenisowy w Leamington Spa .
W grudniu 1873 major Walter Clopton Wingfield opracował podobną grę , w którą grano podczas przyjęć w ogrodzie jego walijskiej posiadłości Nantclwyd .
W 1874 roku major opatentował tę grę nadając jej grecką nazwę `` sphairistike '' , znaną również jako `` lawn tennis '' .
