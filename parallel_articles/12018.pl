Historia .
Początek Type O Negative wiąże się z grupami Fallout oraz Carnivore , które były poprzednimi zespołami Petera Steela .
Podczas jednego z ostatnich koncertów Carnivore , muzyk już brał pod uwagę założenie nowego zespołu i gdy w 1990 roku grupa rozpadła się , wkrótce potem , po namowach przyjaciela i perkusisty Sala Abruscato , powstał zespół Subzero .
Do jego składu zwerbowani zostali gitarzysta Kenny Hickey , zaś dzięki Steelowi , Josh Silver , keyboardzista , który występował poprzednio w formacji Fallout .
W 1991 roku ukazał się debiutancki album zespołu zatytułowany `` Slow Deep and Hard '' .
Nagrania trwały sześć tygodni w studiu Silver Records .
Wkrótce po wydaniu płyty , to jest w listopadzie 1991 roku , Type O Negative rozpoczął serię koncertów wraz z zespołami Biohazard i The Exploited .
W roku 1992 ukazała się nowa płyta o tytule `` The Origin of the Feces '' .
Album w założeniu miał sprawiać wrażenie wydawnictwa koncertowego , jednakże odgłosy publiczności zostały w rzeczywistości nagrane w studiu .
Druga płyta długogrająca powstała w roku 1993 .
Album zatytułowany `` Bloody Kisses '' spotkał się z pozytywnym przyjęciem ze strony krytyków muzycznych oraz wysokim nakładem w Stanach Zjednoczonych .
