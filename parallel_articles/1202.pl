André Villas-Boas
André Villas-Boas , właśc .
Luís André de Pina Cabral de Villas-Boas , wym .
[ ] ( ur .
17 października 1977 w Porto ) – portugalski trener piłkarski .
Kariera szkoleniowa .
Jako nastolatek został włączony do sztabu szkoleniowego FC Porto przez ówczesnego pierwszego trenera , sir Bobby ’ ego Robsona , ze względu na dobrą znajomość języka angielskiego .
Ich współpraca zaczęła się od listu , który 16-letni Villas-Boas upuścił pod drzwiami trenera , mieszkającego w tym samym apartamentowcu .
Zażądał w nim włączenia do pierwszej jedenastki napastnika Domingosa Paciêncii .
Robson spotkał się z chłopakiem i przeczytał przygotowany przez niego raport .
Portugalczyk zrobił takie wrażenie , że został stażystą w klubie , a następnie dzięki Robsonowi skończył praktyki w Ipswich Town i kursy trenerskie w Szkocji .
W wieku siedemnastu lat uzyskał pierwszą licencję trenerską ( UEFA C ) .
