Pałecznica orlicowa
Pałecznica orlicowa ( `` Typhula quisquiliaris '' ( Fr . )
Henn . )
– gatunek grzybów z rodziny pałecznicowatych ( `` Typhulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Typhula , Typhulaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał James Sowerby w 1801 r. , ale zdiagnozował go taksonomicznie dopiero Elias Fries w 1818 roku nadając mu nazwę `` Clavaria quisquiliaris '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Paul Christoph Hennings w 1896 r. przenosząc go do rodzaju `` Typhula '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Maczugowaty , złożony z główki i trzonu .
