Pałecznica sklerotowa
Pałecznica sklerotowa ( `` Typhula sclerotioides '' ( Pers . )
Fr . )
– gatunek grzybów z rodziny pałecznicowatych ( `` Typhulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Typhula , Typhulaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał Christiaan Hendrik Persoon w 1822 r. nadając mu nazwę `` Phacorhiza sclerotioides '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Elias Fries w 1838 r. przenosząc go do rodzaju `` Typhula '' .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Występowanie i siedlisko .
Znane jest występowanie pałecznicy sklerotowej w Europie , Ameryce Północnej i Indiach .
W zestawieniu wielkoowocnikowych grzybów podstawkowych Polski Władysław Wojewoda przytoczył 10 stanowisk tego gatunku z uwagą , że jego rozprzestrzenienie i stopień zagrożenia nie są znane .
Występuje w lasach , na polanach i leśnych drogach , gdzie rozwija się na leżących na ziemi liściach , martwych i próchniejących gałązkach drzew , krzewów i pędach roślin zielnych .
W Polsce notowana na pędach miłosnej górskiej , topoli osiki , robinii akacjowej i śnieguliczki białej .
