Tyta
Tyta ( niem .
`` Schultüte '' , `` Zuckertüte '' ) – zwyczajowy prezent , jaki otrzymują dzieci idące po raz pierwszy do szkoły , do 1 klasy .
Ma formę rogu obfitości wypełnionego słodyczami – odwróconego stożka lub ostrosłupa z kartonu , oklejonego ozdobnym papierem , niekiedy owinięta jest celofanem i związana u góry wstążką .
Zwyczaj obdarowywania pierwszaków tego typu prezentami odnotowano w Niemczech na początku XIX wieku ( potwierdzona w 1817 roku w Jenie ) .
Na terenie Polski znany od lat dwudziestolecia międzywojennego , obecnie występuje na Górnym Śląsku i w Jaworznie , spotykany także w Wielkopolsce i na Warmii .
Na Górnym Śląsku dzieciom z uboższych rodzin zwyczajowe tyty są fundowane przez organizacje społeczne i fundacje .
