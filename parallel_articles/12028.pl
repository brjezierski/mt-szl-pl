Thorshavn ( gmina )
Thorshavn ( duń .
`` Thorshavn kommune '' , far .
`` Tórshavnar kommuna '' ) – największa zarówno pod względem liczby ludności , jak i powierzchni gmina na Wyspach Owczych , terytorium zależnego od Danii .
Przyległymi gminami są : Kvívíkar kommuna , Sunds kommuna ( wyspa Streymoy ) , Skopunar kommuna ( Sandoy ) .
Siedzibą władz gminy jest Thorshavn .
Tórshavnar kommuna leży w południowej części Streymoy .
W jej skład wchodzą także mniejsze wyspy : Hestur , Koltur oraz Nólsoy .
Jej powierzchnia wynosi 172,9 km² .
