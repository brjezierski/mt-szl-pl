Andrés Iniesta
Andrés Iniesta Luján , wym .
[ ] ( ur .
11 maja 1984 w Fuentealbilli ) – hiszpański piłkarz występujący na pozycji pomocnika w japońskim klubie Vissel Kobe .
W latach 2006–2018 reprezentant Hiszpanii .
Złoty medalista Mistrzostw Świata 2010 oraz Mistrzostw Europy 2008 i 2012 .
Życiorys .
FC Barcelona .
Andrés Iniesta do Barcelony dołączył w 1996 roku po turnieju w Brunette , na którym występował w zespole Albacete Balompié .
Po przenosinach do Barcelony tęsknił za swoją rodziną , lecz postanowił pozostać w La Masii .
Jego idolem był Pep Guardiola , którego plakat wisiał w pokoju małego Iniesty .
Szybko okazało się , że ma wielki talent do gry .
Gdy miał 16 lat , został zaproszony na treningi pierwszej drużyny .
„ Ten dzieciak wyśle nas wszystkich na emeryturę ” , powiedział wówczas Pep Guardiola w rozmowie z Xavim Hernándezem .
Iniesta w drużynach juniorskich przeszedł przez kolejne kategorie wiekowe .
Od sezonu 2000/2001 został włączony do kadry FC Barcelona B , a 22 grudnia 2001 roku znalazł się na ławce rezerwowych w ligowym spotkaniu z Espanyolem .
W pierwszym zespole zadebiutował 29 października 2002 , pod wodzą Louisa van Gaala , w spotkaniu z Club Brugge w ramach Ligi Mistrzów .
Szansę gry otrzymał w wyniku kontuzji podstawowego rozgrywającego drużyny Xaviego .
