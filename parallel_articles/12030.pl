Historia .
Tønsberg jest jednym z najstarszych miast w Norwegii .
Pierwsze wzmianki o jego istnieniu pochodzą jeszcze z sag wikingów o królu Haraldzie Pięknowłosym z końca IX wieku n.e .
Oficjalnie za początek historii miasta uznaje się rok 871 , ponieważ według wspomnianej sagi Tønsberg istniał jeszcze przed bitwą pod Hafrsfjordem w 872 roku .
Przez wiele wieków Tønsberg był siedzibą królów .
