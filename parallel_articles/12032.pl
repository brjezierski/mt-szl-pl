Türkmenabat
Türkmenabat ( do 1924 i 1927–1940 Czardżuj , później Czardżou , `` Çärjew '' , ) – miasto we wschodnim Turkmenistanie przy granicy z Uzbekistanem , położone na lewym brzegu Amu-darii ( naprzeciw ujścia jej dopływu Zerawszan ) na wysokości 187 m n.p.m .
