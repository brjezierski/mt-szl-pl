Konkurs Piosenki Turkowizji 2015
3 .
Konkurs Piosenki Turkwizji – impreza muzyczna , która została zorganizowana 19 grudnia 2015 roku w Turcji przez tureckiego nadawcę Turkish Music Box Television ( TMB ) .
Był to drugi konkurs zorganizowany przez turecką telewizję .
Miejsce organizacji konkursu .
22 listopada 2014 roku zostało ogłoszone że Konkurs Piosenki Turkwizji 2015 zostanie zorganizowany w Turkmenistanie .
Mary , stolica prowincji Mary w Turkmenistanie , została ogłoszona jako gospodarz .
Jednak 21 lutego 2015 roku zostało ogłoszone że nowym miejscem gdzie odbędzie się konkurs będzie Aszchabad .
Późnym sierpniem 2015 roku znów zmieniono miasto na Stambuł , Turcja .
Konkurs był organizowany w Yahya Kemal Beyatlı Gösteri Merkezi która ma pojemność na 10 000 osób .
