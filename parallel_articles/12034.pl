Konkurs Piosenki Turkwizji
( ang .
`` Türkvizyon Song Contest '' , tur .
`` Türkvizyon Dünyası Şarki Yarışması '' ) – organizowana od 2013 roku coroczna impreza muzyczna , inspirowana Konkursem Piosenki Eurowizji .
Biorą w nim udział przedstawiciele krajów bądź regionów , na terenie których zamieszkują ludy tureckie lub używane są języki tureckie .
W konkursie uczestniczą też przedstawiciele grup etnicznych używających języka tureckiego .
Organizacją konkursu zajmuje się turecki nadawca publiczny – Türkiye Radyo Televizyon Kurumu .
Dyrektorem generalnym konkursu jest Ismet Zaatov .
Początki .
We wrześniu 2012 roku Turcja zdecydowała , iż nie weźmie udziału w 58 .
Konkursie Piosenki Eurowizji ze względu na niezadowolenie z aktualnych zasad tego konkursu .
Krytykowano między innymi 50 % udział głosów jury w rezultacie końcowym oraz przywileje tak zwanej „ wielkiej piątki ” konkursu .
