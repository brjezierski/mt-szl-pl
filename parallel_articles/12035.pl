Tłuszcz ( zespół muzyczny )
Tłuszcz ( biał .
Тлушч ) – białoruski zespół rockowy , założony w sierpniu 2012 roku w Mińsku przez byłych członków grupy Zawułak Vinni Stigmy – wokalistę i gitarzystę Pawła Biełego i perkusistę Pawła Płutaua oraz przez basistę Andreja Swiryda .
Zespół wydał dotychczas dwa albumy studyjne , dwa minialbumy oraz kilka singli i teledysków .
Teksty piosenek pisane są głównie w języku białoruskim , a większość z nich jest utrzymana w żartobliwej i ironicznej stylistyce .
Grupa koncertuje po Białorusi i krajach ościennych – w swojej historii kilkukrotnie występowała w Polsce , a także wzięła udział m.in .
w festiwalach Bulbana 2013 oraz Basowiszcza 2014 .
Utwór „ Wiecznaść ” nagrany przez Tłuszcz został wykorzystany w polskim serialu szpiegowskim `` Nielegalni '' .
