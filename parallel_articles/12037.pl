Języki tungusko-mandżurskie
Języki tungusko-mandżurskie ( języki tunguskie , języki mandżursko-tunguskie ) – podrodzina języków ałtajskich , którymi posługuje się kilkadziesiąt tysięcy osób zamieszkujących rozległe obszary wschodniej Syberii oraz rosyjskiego i chińskiego Dalekiego Wschodu .
Dzieli się na dwie grupy : tunguską i mandżurską .
Językom tym , ze względu na postępującą asymilację , grozi wymarcie .
