Ludy tunguskie
Ludy tunguskie , Tunguzi – ludy posługujące się językami z rodziny tungusko-mandżurskiej , zamieszkujące północną Azję ( rosyjska Syberia , Mongolia , północne Chiny – zwłaszcza Mongolia Wewnętrzna ) .
Termin „ ludy tunguskie ” ma różne zakresy znaczeniowe w kręgach naukowych różnych krajów :
Pochodzenie nazwy .
Nazwa `` tunguz '' pochodzi od tatarskiego słowa „ świnia ” , oznaczającego też jedzącego wieprzowinę .
Tatarzy wyznający islam nazywali tak plemiona innych religii .
Europejczycy mianem Tunguzów określali Ewenków – najliczniejszą spośród tej grupy językowej narodowość , a następnie także inne , pokrewne im językowo i kulturowo ludy .
Ta niejednoznaczność spowodowała , iż z czasem Ewenków zaczęto określać nazwą opartą na stosowanej w ich własnym języku , a słowo Tunguzi stosowano na określenie grupy ludów .
