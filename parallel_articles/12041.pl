Pomidor zwyczajny
Pomidor zwyczajny , psianka pomidor , pomidor ( `` Solanum lycopersicum '' L. ) – gatunek rośliny z rodziny psiankowatych .
Pochodzi z Peru w Ameryce Południowej .
Został szeroko rozprzestrzeniony i uprawiany jest jako roślina jadalna .
W Polsce ma status zadomowionego antropofita .
Biologia .
W strefie międzyzwrotnikowej rośnie jako bylina , jednak z powodu wrażliwości na mróz w strefie klimatu umiarkowanego gatunek uprawiany jest jako roślina jednoroczna .
Kwiaty są samopylne .
