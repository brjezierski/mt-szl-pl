Tonga ( Południowa Afryka )
Tonga – miejscowość w Południowej Afryce , w prowincji Mpumalanga .
Znajduje się w gminie Nkomazi w dystrykcie Ehlanzeni .
Leży około 90 km na południowy wschód od miasta Nelspruit , w pobliżu granicy z Mozambikiem .
Tonga zajmuje powierzchnię 7,47 km² .
Według spisu ludność przeprowadzonego w 2011 znajdowało się tu 4296 gospodarstw domowych i zamieszkiwało 17 333 osób , spośród których 99,80 % to czarni Afrykanie , natomiast 81,28 % posługiwało się językiem suazi , a 17,53 % tsonga .
