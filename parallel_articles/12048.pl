Liga Mistrzów UEFA ( 2019/2020 )
Liga Mistrzów UEFA 2019/2020 – 28. sezon Ligi Mistrzów UEFA ( 65. turniej ogólnie , wliczając Puchar Europy Mistrzów Klubowych ) .
Rozgrywki zostały przerwane 13 marca 2020 roku z powodu pandemii COVID-19 .
17 czerwca 2020 roku ogłoszono , że wznowienie rozgrywek odbędzie się 7 sierpnia .
Zespoły rozegrały zaległe mecze 1/8 finału a następnie rozegrały po jednym meczu od ćwierćfinału .
Dwa ćwierćfinały , jeden półfinał oraz finał odbyły się na Estádio da Luz w Lizbonie , pozostałe mecze zostały rozegrane na Estádio José Alvalade także w Lizbonie .
Finał został rozegrany 23 sierpnia 2020 roku .
Zwycięzcą został Bayern Monachium po wygranej 1:0 nad Paris Saint-Germain .
Pierwotnie finał miał się odbyć na Atatürk Olympic Stadium w Stambule .
Zwycięzca edycji 2019/20 zakwalifikował się do fazy grupowej Ligi Mistrzów w sezonie 2020/21 oraz rozegra mecz o Superpuchar Europy ze zwycięzcą Ligi Europy UEFA , a także zagra w Klubowych Mistrzostwach Świata .
Rozgrywki składały się z 3 części :
Format rozgrywek i podział miejsc .
Zachowany został format rozgrywek z poprzedniego sezonu .
Miejsca dla federacji zostaną rozdzielane poprzez współczynnik ligowy UEFA z sezonu 2017/18 .
W edycji 2019/2020 Ligi Mistrzów może wziąć udział 81 zespołów z 54 federacji piłkarskich zrzeszonych w UEFA ( poza Liechtensteinem , gdzie nie były przeprowadzane krajowe rozgrywki ligowe – drużyny piłkarskie z tego kraju należą do szwajcarskiego systemu ligowego ) .
