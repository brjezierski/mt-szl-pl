UEFA
Unia Europejskich Związków Piłkarskich ( ang .
`` Union of European Football Associations '' , UEFA ) – międzynarodowa organizacja pozarządowa , zrzeszająca związki piłki nożnej z Europy i z Azji , założona 15 czerwca 1954 w Bazylei , w Szwajcarii .
Organizacja .
UEFA jest organizatorem europejskich pucharów ( Liga Mistrzów UEFA , Liga Europy UEFA , Liga Konferencji Europy UEFA , Superpuchar Europy UEFA ) oraz Mistrzostw Europy , a także Mistrzostw Europy w futsalu , Liga Mistrzów Futsalu i Pucharu Regionów UEFA .
Obecnie jej siedziba znajduje się w mieście Nyon w Szwajcarii .
Organizacja zrzesza 55 federacji narodowych , z Gibraltarem i Kosowem .
Drużyny z 53 krajów występują w męskiej Lidze Mistrzów i Lidze Europy ( poza Liechtensteinem , którego drużyny piłkarskie należą do szwajcarskiego systemu ligowego i Kosowem , którego drużyny nie spełniały warunków licencyjnych w sezonie 2016/17 ) .
W Lidze Mistrzów Kobiet występują kluby z 47 krajowych federacji ( bez Andory , Armenii , Liechtensteinu i San Marina , ligi z Azerbejdżanu i Gruzji zostały zawieszone ) , a Belgia i Holandia mają jedną wspólną ligę ( BeNe League ) .
Do organizacji tej nie należą : 2 państwa Monako , Watykan , 3 terytoria zależne Guernsey , Jersey i Wyspa Man .
Konkurencję dla UEFA w piłce nożnej stanowi Międzynarodowa Wyspiarska Federacja Sportu , a w futsalu UEFS ( Union Europeenne de Futsal ) .
UEFA jest członkiem FIFA .
