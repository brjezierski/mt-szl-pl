Ubuntu ( filozofia )
Ubuntu ( wymowa zulu i xhosa : [ ] ) – subsaharyjska afrykańska i system etyczny skupiający się na relacjach międzyludzkich i pomocy bliźniemu .
Słowo `` ubuntu '' ma swoje korzenie w południowoafrykańskich językach bantu .
Ubuntu uważa się za tradycyjną afrykańską koncepcję .
