Język ubyski
Język ubyski , ubychski lub ubychijski – wymarły język , należący do północno-zachodniokaukaskiej rodziny językowej ( także : abchasko-adygejskiej ) , w skład której wchodzą również języki : abchaski , abazyński , adygejski oraz kabardyjski .
Język używany był przez lud Ubychów , mieszkający pierwotnie w okolicach Soczi , w północno-zachodniej części Kaukazu , nad Morzem Czarnym .
Ubychowie zaliczani byli do plemion czerkieskich .
Po wojnie kaukaskiej i zajęciu ich ziem przez Rosję , Ubychowie , razem z Czerkiesami i Abazynami , zostali w latach 60 .
XIX wieku przymusowo przesiedleni w całości na teren Imperium Osmańskiego .
Ostatni użytkownik języka ubyskiego , Tevfik Esenç , zmarł 7 października 1992 r. we śnie .
Za jego życia język ten badał językoznawca Georges Dumézil .
W języku ubyskim w użyciu były 84 spółgłoski ( najwięcej na świecie nie licząc mlaskowych ) i tylko 2 samogłoski .
