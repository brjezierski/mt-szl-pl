Angela Merkel
Angela Dorothea Merkel z domu Kasner ( wym .
; ur .
17 lipca 1954 w Hamburgu ) – niemiecka fizyk , polityk , w latach 2000–2018 przewodnicząca Unii Chrześcijańsko-Demokratycznej , posłanka do niemieckiego parlamentu ( Bundestagu ) z powiatu Vorpommern-Rügen w kraju związkowym Meklemburgia-Pomorze Przednie .
W latach 2005–2021 kanclerz Niemiec .
Druga po Helmucie Kohlu najdłużej urzędująca kanclerz w Niemczech .
Według czasopisma „ Forbes ” była najbardziej wpływową kobietą świata ( 2011 ) ; Człowiek Roku tygodnika „ Time ” ( 2015 ) .
