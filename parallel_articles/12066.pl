Udine ( stacja kolejowa )
Udine ( włoski : `` Stazione di Udine '' ) – stacja kolejowa w Udine , największa i najważniejsza w regionie Friuli-Wenecja Julijska , we Włoszech .
Znajdują się tu 4 perony .
Ze stacji można udać się bezpośrednim połączeniem do większych miast włoskich , większość pociągów zatrzymuje się na dworcu kolejowym w Mestre ( lądowa część Wenecji ) .
Są również bezpośrednie połączenia do Wiednia i Lublany .
Codziennie odjeżdżają stąd co 30 minut pociągi do Wenecji oraz do Triestu .
Należy do projektu Centostazioni i obsługuje 7,6 mln pasażerów rocznie .
