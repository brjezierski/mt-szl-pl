Uetersen
Uetersen ( duń .
`` Yttersen '' ) – miasto w północnych Niemczech w kraju związkowym Szlezwik-Holsztyn , w powiecie Pinneberg .
Do 31 grudnia 2016 siedziba związku gmin Haseldorf .
Położone ok. 30 km na północ od Hamburga nad rzeką Pinnau , w pobliżu Łaby .
Miasto zajmuje powierzchnię 11,43 km² .
Według danych z 31 grudnia 2019 Uetersen zamieszkiwało osób .
