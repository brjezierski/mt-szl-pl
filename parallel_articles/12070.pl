Ufa ( rzeka )
Ufa ( ros .
`` Уфа́ '' , wym .
`` Ufá '' ; baszk .
`` Ҡариҙел '' ) – rzeka w europejskiej części Rosji , prawy dopływ Biełej .
Ma długość 918 km , a powierzchnia dorzecza wynosi 53 100 km² .
Źródła znajdują się w Uralu Środkowym .
W dorzeczu Ufy położone są złoża ropy naftowej , a przy ujściu - miasto Ufa .
