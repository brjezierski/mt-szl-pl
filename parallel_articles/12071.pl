Etymologia .
Nazwa kraju pochodzi od nazwy królestwa Buganda , które obejmowało część terenów dzisiejszej Ugandy .
Brytyjczycy objęli królestwo protektoratem i przyjęli tę nazwę w swoim języku .
Historia .
Na początku naszej ery część ludów zamieszkujących obecne tereny północnej Ugandy , głównie ludu Bantu , prowadziły osiadły tryb życia i zajmowały się rolnictwem .
W pierwszej połowie I tysiąclecia plemiona te podporządkowali sobie zbrojnie Niloci przybyli z północy .
W X-XI wieku powstało tu państwo Kitwara , którego szczyt rozwoju przypadł na wiek XIV-XV .
Po rozpadzie Kitwary powstało wiele małych państw plemiennych .
Najważniejszymi z plemiennych państw były królestwa Ankole , Bunjoro , Toro i Buganda .
W XIX wieku Buganda objęła hegemonię w regionie .
Wraz z XIX wiekiem pojawili się tu Arabowie , którzy szerzyli tu islam i arabską kulturę .
Szczególny rozkwit nastąpił podczas panowania Mutesy I ( 1860-84 ) .
