Uher
Uher – wieś w Polsce , położona w województwie lubelskim , w powiecie chełmskim , w gminie Chełm .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 256 mieszkańców i była dziewiętnastą co do wielkości miejscowością gminy Chełm .
Krótki opis .
Na przełomie XIX i XX wieku istniał piękny , klasycystyczny dwór Olędzkich który został spalony w 1914 roku przez Kozaków .
