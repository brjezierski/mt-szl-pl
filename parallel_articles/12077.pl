Język ujgurski
Język ujgurski ( zwany też językiem `` nowoujgurskim '' ) – język z rodziny języków tureckich używany przez 11 mln Ujgurów , głównie w Regionie Autonomicznym Sinciang w zachodnich Chinach , a także w północno-wschodnim Kazachstanie .
W Regionie Autonomicznym Xinjiang ma status języka urzędowego .
Do zapisu języka ujgurskiego używa się obecnie pisma arabskiego w redakcji perskiej , przy czym samogłoski obowiązkowo oznacza się specjalnymi znakami diakrytycznymi .
Do XVIII w. zapisywany był pismem ujgurskim , abdżadem zapisywanym z góry na dół .
Jak w wielu innych językach tureckich , w języku ujgurskim występuje harmonia wokaliczna .
