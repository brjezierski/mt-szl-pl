Ujrzanów
Ujrzanów – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 7 km na wschód od Siedlec .
Przez Ujrzanów przebiega trasa międzynarodowa E30 Cork – Omsk i
droga powiatowa do Siedlec .
Wierni Kościoła rzymskokatolickiego należą do parafii Ducha Świętego w Siedlcach .
Wieś szlachecka położona była w drugiej połowie XVI wieku w ziemi łukowskiej województwa lubelskiego .
W latach 1975–1998 miejscowość położona była w województwie siedleckim .
