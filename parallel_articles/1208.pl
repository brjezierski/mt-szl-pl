Język angielski
Język angielski , angielszczyzna ( ang .
, ) – język z grupy zachodniej rodziny języków germańskich , powszechnie używany w Wielkiej Brytanii , jej terytoriach zależnych oraz w wielu byłych koloniach i dominiach , m.in .
Stanach Zjednoczonych , Irlandii , Kanadzie , RPA , Australii i Nowej Zelandii .
Pełni funkcję języka oficjalnego bądź półoficjalnego w ponad 60 krajach .
W Wielkiej Brytanii , Stanach Zjednoczonych i Australii angielski jest `` de facto '' językiem urzędowym , choć formalnie państwa te nie objęły takim statusem żadnego języka .
Angielszczyzna należy do oficjalnych języków ONZ , a od XX wieku jest najczęściej używanym językiem w kontaktach międzynarodowych .
Bywa określana jako pierwszy uniwersalny nowożytny język ludzkości .
Stanowi główny język nauki , handlu i polityki , a w niektórych krajach także kształcenia wyższego .
Angielszczyzna wykazuje silne zróżnicowanie na podłożu geograficznym i socjalnym .
Dialekty języka angielskiego częściowo różnią się fonetyką , słownictwem i gramatyką .
Rolę instytucjonalnej normy porozumiewawczej pełni różnie definiowany dialekt ogólnoangielski ( ) , występujący w różnych podwariantach narodowych .
Język angielski jest obecnie źródłem zapożyczeń prawie na całym świecie , ale wcześniej to angielszczyzna ewoluowała pod wpływem innych języków .
Język angielski to język ojczysty 527 mln ludzi .
Zajmuje trzecie miejsce , za hindi lub urdu ( jedna grupa , języki ojczyste 588 mln ludzi ) oraz chińskim ( 1,39 mld ) .
Uczy się go 1,5 mld osób , dla porównania chińskiego tylko 30 mln .
Angielskim jako swoim pierwszym i głównym językiem posługują się obywatele 101 państw .
Historia .
Angielszczyzna jest językiem zachodniogermańskim , który wywodzi się z anglo-fryzyjskich i jutlandzkich narzeczy przyniesionych do Brytanii przez germańskich osadników i rzymskie wojska z terenu obecnych północnych Niemiec i Holandii .
Na początku język staroangielski był zróżnicowaną grupą dialektów , obrazującą różnorodne źródła anglosaskiej ludności ówczesnej Anglii .
Jeden z tych dialektów , późny zachodniosaski , w końcu zyskał przewagę .
Pierwotny staroangielski był następnie pod wpływem obcym ze względu na dwie fale inwazji .
Pierwsza to inwazja wikingów , użytkowników języków skandynawskich ; zdobyli oni i skolonizowali część Brytanii w ósmym i dziewiątym wieku .
Druga to inwazja Normanów w XI wieku .
Mówili oni językiem starofrancuskim i rozwinęli anglo-normańską odmianę języka angielskiego .
Te dwie inwazje spowodowały , że angielszczyzna stała się do pewnego stopnia językiem mieszanym .
Współżycie ze Skandynawami zaowocowało zauważalnym gramatycznym uproszczeniem i leksykalnym wzbogaceniem anglo-fryzyjskiego rdzenia angielszczyzny ; późniejsza normańska okupacja prowadziła ku przeszczepieniu do rdzenia germańskiego bardziej wyszukanej warstwy słów z romańskiej gałęzi języków europejskich .
Ten wpływ normański wprowadził angielszczyznę do sądów i urzędów .
W ten sposób angielszczyzna ewoluowała w „ pożyczający ” język o wielkiej elastyczności i bogatym słownictwie .
