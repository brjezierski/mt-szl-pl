Gramatyka języka angielskiego .
Język angielski jest językiem pozycyjnym i analitycznym ( duże znaczenie odgrywają czasowniki posiłkowe ) , choć występują też pewne elementy fleksji .
Są to relikty indoeuropejskiego systemu deklinacji i koniugacji , w pełni zachowanego w językach słowiańskich i łacinie , a częściowo w staroangielskim .
Odmiana przez przypadki dotyczy zaimków , rzeczowniki posiadają tylko dopełniacz ( `` Saxon genitive '' ) , tworzony z końcówką – ’ s .
W języku angielskim występują przedimki : określony `` the '' i nieokreślony `` a ( n ) '' .
Formy `` an '' używa się przed wyrazami wymawianymi z nagłosową samogłoską – `` an hour '' , ale `` a union '' .
Co niezwykłe wśród języków indoeuropejskich , nie istnieje kategoria rodzaju gramatycznego , z wyjątkiem zaimków trzeciej osoby liczby pojedynczej .
Przedimek .
Przedimek w języku angielskim to nieodmienna część mowy determinująca rzeczownik .
Przedimka zwykle nie tłumaczy się na język polski .
Czasami można go oddać jako zaimek ten , ta , to ( przedimek określony ) , jakiś , jakaś , jakieś ( przedimek nieokreślony ) .
Przedimkiem jest wyraz występujący przed rzeczownikiem albo frazą rzeczownikową ( np .
przymiotnik + rzeczownik ) .
W języku angielskim rozróżnia się dwa rodzaje przedimków :
Przedimek ( zwany `` article '' lub `` determiner '' ) określa , czy desygnaty są znane mówiącemu i rozmówcy .
Jeśli są znane , używa się przedimka określonego .
W przeciwnym wypadku stosowany jest przedimek nieokreślony .
Przedimków `` a/an '' używa się wyłącznie przed rzeczownikiem policzalnym w liczbie pojedynczej , Oznacza on dowolny element z danego zbioru , innymi słowy pojedynczy choć nie wyjątkowy element : `` My father is a teacher '' → `` Mój ojciec jest nauczycielem '' .
Jest jednym ze zbioru nauczycieli .
Przedimek określony `` the '' jest najpopularniejszym słowem angielskim .
Kontrastuje z przedimkiem nieokreślonym `` a '' i z przedimkiem zerowym .
`` The '' ma zawsze tę samą formę .
Przedimek określony stosuje się do opisu określonego przedmiotu , osoby bądź stanu .
