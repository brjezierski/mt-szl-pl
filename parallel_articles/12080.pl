Wiłkomierz
Wiłkomierz ( `` lit .
Ukmergė '' , dawniej `` Vilkmergė '' ) – miasto rejonowe w środkowej Litwie położone ok. 60 km na północny zachód od Wilna .
Ludność : 28 tys .
mieszkańców ( 2005 ) .
Jest jednym z najstarszych miast na Litwie .
Wzmianka o zamku w Wiłkomierzu pochodzi z 1225 .
Prawa miejskie nadano na początku XV wieku .
Miejsce obrad sejmików ziemskich powiatu wiłkomierskiego od XVI wieku do pierwszej połowy XVIII wieku .
Historia .
Na Górze Zamkowej ślady grodziska z zamkiem , pozostałość jednego z najstarszych grodów litewskich .
Podobno istniał od X wieku .
