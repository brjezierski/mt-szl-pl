Nie licząc powyższych dwóch jednostek językowych , najbliższy językowi ukraińskiemu jest język białoruski ; standardowa ukraińszczyzna nie jest natomiast wzajemnie zrozumiała z językiem rosyjskim .
Ukraiński wyodrębnił się między XIII a XVII wiekiem z języka ruskiego , a w XVIII wieku ukształtował się jego standard literacki .
Język ukraiński bywał zwalczany i rusyfikowany w czasach carskich i sowieckich , jednak od czasu niepodległości Ukrainy cieszy się swobodą rozwoju i rosnącą popularnością .
Wywarł wpływ na inne języki , w tym polski , w którym można znaleźć dziesiątki ukrainizmów .
Zapis języka ukraińskiego od początku opierał się na cyrylicy i powstały jej różne warianty dla tego języka , tak jak różne latynizacje .
Od początku XX wieku alfabet ukraiński uległ niewielu zmianom ; największą było przywrócenie w latach 90. jednej litery .
