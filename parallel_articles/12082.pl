Język ukraiński
Język ukraiński ( ukr .
, ) – język z grupy wschodniosłowiańskiej .
W latach 20 .
XXI wieku posługuje się nim ponad 40 mln ludzi , co czyni go drugim najpopularniejszym w tej grupie i trzecim najpopularniejszym w rodzinie słowiańskiej .
Używany jest głównie na terenie Ukrainy , gdzie ma status języka urzędowego , a ponad 3/4 mieszkańców zadeklarowało go jako język ojczysty .
Występuje także w krajach sąsiednich – jak Rosja , Białoruś , Polska i Mołdawia – oraz dalszych jak Stany Zjednoczone i Kanada .
Język ukraiński obejmuje co najmniej kilkanaście gwar grupowanych w trzy dialekty .
