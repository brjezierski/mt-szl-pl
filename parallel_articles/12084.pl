Układ współrzędnych
Układ współrzędnych – odwzorowanie wzajemnie jednoznaczne przypisujące każdemu punktowi przestrzeni formula_1 skończony ciąg ( n-krotkę ) liczb rzeczywistych zwanych współrzędnymi punktu formula_2 .
