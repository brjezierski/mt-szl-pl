Ulsan
Ulsan ( kor .
울산광역시 , Metropolia Ulsan ) – miasto na prawach metropolii w południowo-wschodniej części Korei Południowej .
Położone jest nad brzegiem Morza Wschodniego ( `` Morze Japońskie '' ) , 70 km na północ od Pusan .
Znajduje się tu Uniwersytet Ulsan .
Podział administracyjny .
Ulsan podzielony jest na 4 dzielnice ( kor .
`` gu '' ) i 1 powiat ( kor .
`` gun '' ) .
Gospodarka .
Miasto jest sercem narodowej strefy przemysłowej zwanej `` Obszar przemysłowy Ulsan '' , w którym siedzibę ma m.in .
międzynarodowy koncern Hyundai .
Do 1962 roku Ulsan był portem rybackim .
W trakcie realizacji pięcioletniego planu ekonomicznego w Korei Południowej został przemianowany na port handlowy , przeładunkowy .
W tym samym roku włączono do metropolii port Pangojin .
Sport .
W mieście siedzibę ma klub piłkarski koreańskiej pierwszej ligi K-League Ulsan Hyundai Horang-i .
W mieście znajduje się także hala sportowa Dongcheon Arena .
