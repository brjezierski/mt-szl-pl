Ulucz
Ulucz ( 1565 `` Hulicz '' , 1872 `` Ułucz '' , w latach 1977–1981 Łąka ) – osada w Polsce położona w województwie podkarpackim , w powiecie brzozowskim , w gminie Dydnia .
Leży nad rzeką San .
Historia .
Pierwsze ślady osadnictwa w Uluczu przypadają na okres kultury trzcinieckiej – epoki brązu , a zgromadzone zabytki archeologiczne przez profesora Michała Parczewskiego z tego terenu datowane są w czasie na około 1600 lat p.n.e .
W samym Uluczu archeolodzy odkryli wielokulturową osadę z zabytkami z epoki brązu oraz okresu halsztackiego .
