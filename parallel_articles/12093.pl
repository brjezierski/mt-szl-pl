Omar Ibn Abi Rabi ' a
Omar Ibn Abi Rabi ' a , właściwie Umar Ibn Abi Rabi ' a ( ur .
w 644 r. w Mekce , zm .
w 719 r. tamże ) – poeta arabski , jeden z największych liryków klasycznej literatury arabskiej , `` beduiński '' piewca gorącej i beznadziejnej miłości ; pierwszy przedstawiciel ( wraz z Dżamilem ) ghazalu ( liryki miłosnej ) .
Jego utwory są cennym źródłem poznania społecznej pozycji kobiet arabskich .
