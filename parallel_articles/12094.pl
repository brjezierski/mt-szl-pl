Umaru Yar ’ Adua
Alhaji Umaru Musa Yar ’ Adua ( ur .
16 sierpnia 1951 w Katsinie , zm .
5 maja 2010 w Abudży ) – nigeryjski polityk , gubernator stanu Katsina w latach 1999–2007 .
Członek Ludowej Partii Demokratycznej ( PDP ) .
Prezydent Nigerii od 29 maja 2007 do śmierci .
Umaru Yar ’ Adua , z wykształcenia chemik , po zakończeniu studiów rozpoczął pracę naukową i akademicką .
W latach 80. i 90 .
XX w. pracował w sektorze prywatnym .
W działalność polityczną zaangażował się szerzej w latach 90 .
W 1999 został gubernatorem północnego stanu Katsina , uzyskując cztery lata później reelekcję na stanowisku .
Pod koniec 2006 został wybrany przez rządzącą Ludową Partię Demokratyczną kandydatem na urząd prezydenta .
W kwietniu 2007 odniósł zwycięstwo w wyborach prezydenckich , których wiarygodność została jednakże zakwestionowana przez międzynarodowych obserwatorów .
29 maja 2007 został zaprzysiężony na stanowisku prezydenta Nigerii .
Jako prezydent podejmował działania na rzecz ograniczenia korupcji .
Zapoczątkował zmiany w prawie wyborczym , opracował plan zwiększenia produkcji energii elektrycznej oraz zmian w przemyśle naftowym i gazowym .
W celu wzmocnienia bezpieczeństwa wewnętrznego zaangażował się w proces rozwiązania konfliktu w Delcie Nigru .
W 2009 przedstawił walczącym tam ugrupowaniom ofertę amnestii i rozbrojenia , z której skorzystało w sumie ok. 20 tys .
bojowników .
W polityce zagranicznej skupiał się główne na sprawach afrykańskich i regionalnych .
W 2008 doprowadził do zakończenia sporu granicznego z Kamerunem .
