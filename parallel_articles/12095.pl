Umbrtka
Umbrtka – side-project czeskiego wykonawcy Morbivoda ( inne jego projekty to zespoły Trollech , Quercus , War for War i Stíny Plamenů ) .
Zespół w utworach porusza m.in .
tematykę problemów pracowniczych , społecznych oraz kwestie związane z industrializacją .
W trówczości można znalaźć wiele odnośników do rodzinnego miasta zespołu – Pilzna .
Tytułowa Umbrtka to boska personifikacja przemysłowego miasta .
Muzycznie twórczość zespołu nawiązuje do black metalu , jednak członkowie grupy określają ten styl jako gray metal .
Charakterystyczną cechą zespołu jest krzykliwy wokal ( scream ) , który miesza się z recytacjami oraz klasycznym growlingiem .
Na początku działalności , aby uzyskać bardziej surowe brzmienie , zamiast zestawu perkusyjnego zespół korzystał z blaszanych talerzy i kubła na śmieci .
