Umeå
Umeå – miasto w północnej Szwecji , ośrodek administracyjny regionu ( län ) Västerbotten , port nad rzeką Ume , w pobliżu jej ujścia do Zatoki Botnickiej .
Miasto powstało w 1664 roku .
Dziś Umeå jest ważnym ośrodkiem edukacyjnym , prowadzone są tam badania z zakresu techniki i medycyny .
Znajdują się tam również dwa uniwersytety , na których kształci się ponad 30 000 studentów .
Jest to największe miasto w północnej Szwecji i liczy 84 761 mieszkańców ( 2016 ) a gmina Umeå 110 106 mieszkańców ( 2016 ) .
4,5 kilometra na południe od miasta znajduje się port lotniczy Umeå .
25 czerwca 1888 wschodnia część miasta została doszczętnie spalona .
Podczas odbudowy ze względów przeciwpożarowych ulice zostały obsadzone brzozami – z tego właśnie powodu o mieście mówi się `` Björkarnas stad '' , czyli `` Miasto brzóz '' .
Umeå jest jednym z najszybciej rozwijających się szwedzkich miast .
Miasto stanowi centrum badań naukowych , sztuki oraz kultury .
W 2010 miasto zostało wyróżnione – wraz z łotewską Rygą – tytułem Europejskiej Stolicy Kultury na rok 2014 .
