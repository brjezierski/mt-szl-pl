Transkrypcja .
W języku niemieckim przy niedostępności odpowiednich czcionek lub w adresach internetowych umlauty są zastępowane przez dodanie litery `` e '' po literze ze znakiem diakrytycznym oznaczającym umlaut , czyli :
Przykłady : Franz Neuländtner → Franz Neulaendtner , Gerhard Schröder → Gerhard Schroeder , Müller → Mueller , für → fuer , ( Zugvögel → Zugvoegel ) .
Równolegle do pisowni z wykorzystaniem liter `` ä '' , `` ü '' , `` ö '' występują słowa zawsze pisane przez `` ae '' , `` ue '' , `` oe '' – dotyczy to przede wszystkim niektórych nazwisk ( np .
Goethe , równoległe postacie Schröder i Schroeder ) .
Zapis `` ä '' , `` ö '' i `` ü '' jako `` ae '' , `` oe '' i `` ue '' pochodzi stąd , że dawniej nie stosowano kropek , ale nadpisywano małą literkę `` e '' nad odpowiednią samogłoską .
W niemieckim odręcznym piśmie gotyckim pisana litera `` e '' , które po skróceniu przekształciły się w dwie kropki .
