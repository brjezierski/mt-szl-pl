Anggun
Anggun , właśc .
Anggun Cipta Sasmi ( ur .
29 kwietnia 1974 w Dżakarcie ) – francuska piosenkarka i autorka tekstów pochodzenia indonezyjskiego .
W 1986 wydała swój pierwszy rockowy album `` Dunia Aku Punya '' .
Rozgłos zapewniło jej wydanie singla „ Mimpi ” w 1989 .
Wykorzystała to wyróżnienie , wydając kilka singli i trzy nowe albumy studyjne , które ustabilizowały jej pozycję na scenie i zapewniły jej opinię jednej z czołowych indonezyjskich gwiazd rocka na początku lat 90 .
W 1994 opuściła Indonezję , by kontynuować międzynarodową karierę .
Po dwóch latach spędzonych w Londynie i Paryżu spotkała francuskiego producenta Ericka Benziego i podpisała umowę z Sony Music Entertainment .
Wydała swój pierwszy międzynarodowy album `` Snow on the Sahara '' ( 1997 ) , który był dostępny w 33 krajach na świecie .
Na płycie znalazł się przebój Anggun „ Snow on the Sahara ” , który trafił na pierwsze miejsca list przebojów w kilku państwach .
Od czasu przełomu jej międzynarodowej kariery , piosenkarka wydała pięć albumów studyjnych w języku angielskim i francuskim oraz ścieżkę dźwiękową do duńskiego filmu „ Open Hearts ” ( 2002 ) .
