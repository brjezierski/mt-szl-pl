Undertale
Undertale ( zapisywane jako UNDERTALE ) – komputerowa gra fabularna , wyprodukowana przez niezależnego twórcę Toby ’ ego Foxa .
Inspiracją do stworzenia gry był `` EarthBound '' .
Gra została wydana 15 września 2015 w angielskiej wersji językowej na platformach Microsoft Windows , macOS i Linux .
15 sierpnia 2017 pojawiła się wersja na konsole PlayStation 4 i PlayStation Vita , natomiast 18 września 2018 wydano wersję na konsolę Nintendo Switch .
16 marca 2021 ukazała się wersja na Xbox One .
Rozgrywka .
Akcja gry rozgrywa się w podziemiach zamieszkanych przez potwory .
Główny bohater o imieniu Frisk musi wykonywać kolejne zadania ( łamigłówki ) i pokonywać napotkane potwory , aby wydostać się z podziemi .
Każdy z napotkanych potworów ma własną osobowość , np .
szkielet o imieniu Sans uwielbia żartować , a duch Napstablook cierpi na depresję .
Potwory można pokonać , nie zabijając ich , a poprzez interakcję z nimi , np .
zastraszanie , flirtowanie , komplementowanie czy pocieszanie .
Wybory sposobu interakcji z potworami wpływają na przebieg gry , a także na przebieg ponownych jej rozgrywek ( potwory pamiętają starcia z graczem z poprzednich rozgrywek ) .
Wybór opcji „ genocide run ” sprawia , że gra zyskuje elementy horroru .
Walka z potworami odbywa się w osobnym okienku , gdzie gracz porusza się czerwonym serduszkiem , unikając ataków przeciwnika .
Odbiór gry .
Gra spotkała się z pozytywnym odbiorem recenzentów , uzyskując w wersji na komputery osobiste według serwisu Metacritic średnią z 43 recenzji wynoszącą 92/100 punktów .
Dało jej to trzecie miejsce w rankingu najlepszych gier na PC w 2015 roku i 43. miejsce w rankingu wszech czasów .
Według agregatora GameRankings gra uzyskała średnią z 28 ocen wynoszącą 94,11 % .
Podczas Independent Games Festival w 2016 roku gra została jedną z sześciu finalistek i zdobyła nagrodę publiczności .
W trakcie spotkania twórców internetowych z papieżem Franciszkiem w maju 2016 roku , vlogger Matthew Patrick , właściciel kanału `` The Game Theorists '' , podarował papieżowi kod do ściągnięcia gry `` Undertale '' z platformy Steam , a motywy swojej decyzji przedstawił w filmie `` Why I Gave the Pope Undertale ! ''
w serwisie YouTube .
