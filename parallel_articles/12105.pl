Budynki .
Plany produkcyjne Universal Studios rozłożone są w 100 budynkach Universal City Plaza Drive mieszczących się w Universal City , w Kalifornii na niepołączonym korporacyjnie odcinku ziemi pomiędzy Los Angeles a Burbank .
Ponadto grupa zajmuje część biur operacyjnych w Nowym Jorku w wieży GE Building ( siedziba General Electric , jednego z właścicieli NBC Universal obok Vivendi Universal ) .
Pożary .
W listopadzie 1990 roku pożar strawił dekoracje przedstawiające ulice Nowego Jorku oraz dekoracje wykorzystane w filmie `` Ben-Hur '' .
Odbudowa zbiorów trwała wiele lat , a jej koszt wyniósł 50 milionów dolarów .
1 czerwca 2008 roku wczesnym ranem na terenie wytwórni wybuchł pożar , który zaprószyła ekipa remontowa reperująca dach jednego z budynków .
Płomienie zniszczyły wieżę zegarową z filmu `` Powrót do przyszłości '' , dekoracje z filmu `` Żądło '' oraz pawilon wystawowy `` King Konga '' .
Zniszczeniu uległo także około 50 tysięcy taśm filmowych , a także dekoracje przedstawiające ulice Nowego Jorku .
Pożar opanowano po ponad 12 godzinach .
W akcji brało udział 500 strażaków .
