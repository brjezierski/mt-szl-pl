Universal Pictures
Universal Pictures ( oryginalna nazwa : Universal City Studios LLC , znana także jako Universal Studios , dawniej Universal Film Manufacturing Company i Universal-International Pictures Inc. ) – amerykańska wytwórnia filmowa założona 30 kwietnia 1912 roku przez Carla Laemmle .
Obecnie należy do NBCUniversal , części koncernu Comcast .
Razem z Paramount , Waltem Disneyem oraz Warner Brothers należy do najstarszych i największych wytwórni filmowych w USA .
Od początku istnienia studia do 1 stycznia 2010 roku marką Universal opatrzono łącznie około 3400 filmów .
Wytwórnia często jest błędnie określana jako Universal Studios , które w rzeczywistości jest grupą skupiającą studia filmowe należące do NBC Universal o pełnej nazwie Universal Studios Entertainment .
