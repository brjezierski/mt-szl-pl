Obelzanki
Obelzanki – wieś sołecka w Polsce położona w województwie wielkopolskim , w powiecie szamotulskim , w gminie Wronki , przy drodze wojewódzkiej nr 140 .
W skład sołectwa Obelzanki wchodzą wsie : Obelzanki , Nadolnik , Nowy Kraków , Piła , Smolnica .
W latach 1975–1998 miejscowość administracyjnie należała do województwa pilskiego .
Obelzanki są dawną osadą olęderską , założoną w XVIII w. , dla kolonizacji terenów leśnych .
Na pola tej wsi w nocy 15/16 VIII 1944 r. zrzucono grupę zwiadowców Mikołaja Kozubowskiego .
Miejscowi Niemcy ujęli wtedy jednego ze spadochroniarzy .
