Oborniki
Oborniki – miasto w województwie wielkopolskim , w powiecie obornickim , siedziba władz gminy miejsko-wiejskiej Oborniki oraz powiatu obornickiego .
Leży nad rzeką Wartą u ujścia rzeki Wełny .
W połowie 2012 roku miasto miało 18 416 mieszkańców .
Oborniki uzyskały lokację miejską przed 1292 rokiem .
Miasto królewskie lokowane w 1312 roku położone było w XVI wieku w województwie poznańskim .
Geografia .
Gmina Oborniki znajduje się 25 km na północ od Poznania nad Wartą i ujściem Wełny do Warty .
Gmina zajmuje powierzchnię 340,2 km² .
