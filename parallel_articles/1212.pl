Angloameryka
Angloameryka , Ameryka Anglosaska – określenie na część Ameryki położoną na północ od Meksyku , używane w opozycji do terminu Ameryka Łacińska .
Angloameryka obejmuje Kanadę i Stany Zjednoczone , czyli kraje o przeważającej kulturze anglosaskiej – dominujący język angielski , wyznania protestanckie .
Angloameryka zajmuje obszar ponad 19,3 mln km2 , z ludnością sięgającą 330 mln mieszkańców ( 2006 ) .
Społeczność Angloameryki w 76,1 % zamieszkuje ośrodki miejskie ( 1997 ) .
