Obszary ( Bielsko-Biała )
Obszary – dzielnica zwyczajowa Bielska-Białej położona między Białą a Komorowicami .
Jej obszar pokrywa się z granicami osiedla ( jednostki pomocniczej gminy ) o nazwie Biała Północ , będąc jednocześnie podzielonym między obręby ewidencyjne Komorowice Krakowskie , Lipnik i Hałcnów .
Dla całości lub części dzielnicy stosuje się również nazwy Zabawa oraz Osiedle Rosta .
Przeważa tu zabudowa jednorodzinna , z wyjątkiem części południowej i zachodniej , która ma charakter przemysłowy .
Położenie .
Granice osiedla Biała Północ wyznaczają :
Granice dawnych wsi , na pograniczu których dzielnica się rozwinęła , odzwierciedlają obręby ewidencyjne .
Zdecydowana większość Obszarów należy do obrębu Komorowice Krakowskie .
Ulica Bukietowa , kompleks przemysłowy Nemak Poland przy ulicy Komorowickiej oraz obszar na południe od ulicy Konwaliowej i rzeki Krzywej wokół stacji kolejowej Bielsko-Biała Wschód są częścią obrębu Lipnik .
Niewielki fragment pomiędzy ulicą Czerwoną , Niepodległości i Baczyńskiego należy do obrębu Hałcnów .
Równolegle do nazwy `` Obszary '' funkcjonują zwyczajowo i na mapach określenia `` Zabawa '' oraz `` Osiedle Rosta '' .
Zabawa często odnoszona jest do części zachodniej wokół ulicy Komorowickiej , a Osiedle Rosta do zabudowań na północ od kościoła Matki Bożej Fatimskiej , lecz nie jest to regułą .
Historia .
Dzielnica rozwinęła się na pograniczu dawnych wsi Komorowice ( Krakowskie ) , Lipnik i Hałcnów .
