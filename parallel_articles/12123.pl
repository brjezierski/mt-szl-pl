Obwód kaliningradzki
Obwód kaliningradzki ( ros .
Калининградская область , `` Kaliningradskaja obłast´ '' ) – jednostka administracyjna Federacji Rosyjskiej , rosyjska eksklawa w Europie Środkowej , nad Morzem Bałtyckim .
Geografia .
Położenie i powierzchnia .
Obwód zajmuje obszar 15 096 km² ( ok. 13 300 km² bez powierzchni zatok morskich ) i położony jest nad Bałtykiem .
Obwód kaliningradzki jest najdalej na zachód wysuniętym regionem Rosji .
Teren obwodu oddzielony jest od reszty kraju obszarami niepodległych państw i pozbawiony łączności lądowej z główną częścią państwa rosyjskiego , i jako taki jest eksklawą .
Obwód graniczy z Litwą i Polską .
Do obwodu należy odcinek wybrzeża Morza Bałtyckiego o długości 147 km .
Największa rozciągłość obwodu z północy na południe to 108 km , a ze wschodu na zachód – 205 km .
Obwód kaliningradzki obejmuje historyczne krainy : Sambię , Prusy Dolne ( Natangia i Barcja ) oraz Litwę Mniejszą zwaną Małą Litwą lub Pruską Litwą .
Około ⅓ powierzchni obwodu ( w tym znaczną część wybrzeża oraz całą rosyjską część Mierzei Wiślanej ) stanowią strefy ograniczonego dostępu .
Również 5-kilometrowy pas przy granicy objęty jest zakazem wstępu cudzoziemców ; mogą oni znajdować się tam wyłącznie na podstawie specjalnych przepustek .
Przynależność administracyjna .
Pod względem administracyjnym obwód wchodzi w skład utworzonego w 2000 r. Północno-Zachodniego Okręgu Federalnego .
Strefa czasowa .
Obwód należy do kaliningradzkiej strefy czasowej ( KSK ) .
UTC +2:00 przez cały rok .
Wcześniej , przed 27 marca 2011 , obowiązywał czas standardowy ( zimowy ) strefy UTC+2:00 i czas letni – UTC+3:00 .
