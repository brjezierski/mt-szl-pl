Ochodze
Ochodze ( dodatkowa nazwa w j. niem .
Ochotz , w latach 1936-1945 `` Frühauf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie opolskim , w gminie Komprachcice .
Od 1950 miejscowość należy do województwa opolskiego .
Z Opola dojazd autobusami miejskimi linii nr 8 .
Nazwa .
W XII wieku wydzielanie majątku czy kompleksu majątków z ogólnej jurysdykcji wymagało nie tylko udzielenia przywilejów ( tutaj wystarczało właściwie ogłoszenie na wiecu książęcej decyzji ) , ale wyraźnego określenia obszaru objętego immunitetem .
Było to tym bardziej konieczne , że wydzielony majątek , a zwłaszcza obszar przeznaczony dopiero do skolonizowania , należało wyodrębnić ze struktury opolnej ; z „ książęcych ” terenów leśnych korzystała bowiem ludność określonych opoli , roszcząc sobie do nich uświęcone dawną tradycją prawa .
Na miejsce skomplikowanych powiązań i roszczeń pojawiła się granica linearna , wyodrębniająca objęty immunitetem obszar .
Wytyczenie tej granicy dokonywał książę , często osobiście albo za pośrednictwem wysokich urzędników , obchodząc lub objeżdżając wydzielany teren i wyznaczając granice przez naciosy na drzewach lub wystawianie kamieni granicznych .
Taki sposób wytyczania granic , jak również sam ograniczony obszar nazywano ochodzą lub ( częściej ) ujazdem ( łac .
`` circuitio '' i `` circumequitatio '' ) .
Według niemieckiego językoznawcy Heinricha Adamy ’ ego nazwa miejscowości pochodzi od polskiej nazwy oznaczającej chęć - `` ochoty '' .
W swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu jako najstarszą zanotowaną nazwę miejscowości , wcześniejszą od niemieckiej wymienia `` Ochoc '' , które tłumaczy jako `` Schmausort , Lustort '' czyli po polsku `` Miejscowość ochoty , ucztowania '' .
Niemcy zgermanizowali nazwę na `` Ochotz '' w wyniku czego utraciła ona swoje pierwotne znaczenie .
Po wojnie polska administracja spolonizowała zgermanizowaną nazwę na `` Ochodze '' .
Obecnie obie nazwy nie wiążą się z pierwszym znaczeniem .
Historia .
Pierwsze wzmianka o wsi pochodzi z 1295 .
Wiadomo , że mieszkańcy płacili wówczas dziesięcinę ( zboże ) dla kościoła w Chrząszczycach , gdyż miejscowość należała do tamtejszej parafii .
Nazwa miejscowości najprawdopodobniej pochodzi od śląskich słów `` ochodza '' lub `` ochoza '' – oznaczały one miejsce , które należy obejść .
Według innej teorii określano tak również osady , których granice ustalano przez wspólny obchód z sąsiadami .
W dokumentach pojawiały się również nazwy `` Ochoze '' i `` Ochoc '' , a w Liber fundationis episcopatus Vratislaviensis ( około 1300 ) nazwa `` Ochoss '' .
W 1536 wieś kupili bracia Buchta , razem z sąsiednim Domeckiem .
Niewiele wiadomo o późniejszym okresie .
W 1783 właścicielem wsi miał być niejaki Koschützki .
W 1845 mieszkały w niej 372 osoby , w 1890 – 570 , a pod koniec pierwszej dekady XX wieku 638 osób .
W 1921 podczas plebiscytu za pozostaniem w granicach Niemiec głosowało 338 mieszkańców , a za Polską 121 .
Pod koniec lat 20. w Ochodzach mieszkało 761 katolików i 8 ewangelików .
W pobliskiej leśniczówce Frühauf znajdował się pomocniczy urząd leśny nadleśnictwa Prószków .
Nazwę leśniczówki przyjęto za nazwę całej wsi w 1936 , kiedy to władze hitlerowskie zmieniły wiele nazw miejscowości na brzmiące `` bardziej niemiecko '' ( na polskiej przedwojennej mapie nazwa wsi brzmiała `` Ochodza '' ) .
Tuż przed wybuchem II wojny światowej liczba mieszkańców przekroczyła 1000 osób .
Ochodze posiadały w przeszłości własny herb – znajdował się on na pieczęciach gminnych .
Przedstawiał on skaczącego jelenia oraz drzewo i stojącego obok niego mężczyznę ( myśliwego lub leśniczego ) .
Miał prawdopodobnie nawiązywać do pobliskich lasów oraz istniejącej w nich leśniczówki .
