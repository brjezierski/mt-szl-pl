Ogrodniki ( sielsowiet Ogrodniki )
Ogrodniki ( biał .
`` Агароднікі '' , `` Aharodniki '' ) – agromiasteczko na Białorusi , w rejonie kamienieckim obwodu brzeskiego .
Miejscowość wchodzi w skład sielsowietu Ogrodniki i jest jednocześnie jego siedzibą .
Ogrodniki leżą 40 km na zachód od Kamieńca , 36 km na północny zachód od Brześcia i 7 km na południe od stacji kolejowej Wysoka-Litowsk na linii Brześć-Białystok oraz w pobliżu autostrady Wysokie-Prużana-Słonim ( P85 ) .
Przez miejscowość przepływa rzeka Pulwa .
We agromiasteczku znajduje się dwór rodziny Potockich `` Aleksandria '' z XIX w .
W miejscowości działa przedszkole , szkoła podstawowa , klub , poczta , punkt medyczno-położniczy oraz znalezisko archeologiczne ( wioska z okresu kultury pomorskiej 4000-2000 lat p.n.e .
) .
