Ojcze nasz
Ojcze nasz , Modlitwa Pańska , Modlitwa wzorcowa ( łac .
`` Oratio Dominica , Pater Noster '' ) – najstarsza , a zarazem najważniejsza , modlitwa chrześcijan .
Według Nowego Testamentu to Jezus przekazał ją swoim uczniom .
Jest to formuła chwaląca wspaniałość Boga , jednocześnie mająca formę błagalną .
Według Ojców Kościoła stanowi wzór modlitwy chrześcijańskiej .
`` Ojcze nasz '' jest często używanym narzędziem w językoznawstwie porównawczym .
Widać w niej analogie do modlitwy judaistycznej `` Osiemnaście błogosławieństw '' .
Do naszych czasów na piśmie zachowały się trzy teksty mówiące o Modlitwie .
Pierwszy to Ewangelia Mateusza 6,9-13 , gdzie modlitwa przekazana jest w czasie kazania na górze , drugim tekstem jest Ewangelia Łukasza 11,1-4 , gdzie Jezus uczy modlitwy według prośby ucznia `` Panie , naucz nas modlić się , jak Jan nauczył swoich uczniów '' .
Trzecim tekstem jest Didache , któremu najbliżej jest do Mateusza , gdyż różni się tylko doksologią .
Historia .
Najstarszy zapis modlitwy „ Ojcze nasz ” w języku polskim zachował się w „ Statutach Wacława Księcia Legnickiego ” z roku 1412 .
Najstarsza polska wersja drukowana tej modlitwy ukazała się natomiast w 1475 roku w Statuta synodalia episcoporum Wratislaviensium wydrukowanych we Wrocławiu .
Została wydrukowana wraz z modlitwami „ Zdrowaś Mario ” i „ Wierzę w Boga ” w tzw .
„ `` Statutach Elyana '' ” ( łac .
`` Statuta Synodalia Episcoporum Wratislaviensium '' ) wydrukowanych w pierwszej oficynie wrocławskiej , Drukarni Świętokrzyskiej Kaspra Elyana .
Papież Franciszek zatwierdził zmianę włoskiego przekładu Mszału , w tym słów modlitwy „ i nie wódź nas na pokuszenie ” .
Uważa on , że zwrot ten był źle przetłumaczony , gdyż to nie Bóg nas kusi .
Zmiana ta pozostaje bez bezpośredniego wpływu na teksty Modlitwy Pańskiej w księgach liturgicznych w innych językach ; w języku łacińskim wersją obowiązującą jest nadal : „ `` ne nos inducas in tentationem '' ” .
Ewentualne zmiany tłumaczeń w innych językach pozostają w gestii kościołów narodowych .
