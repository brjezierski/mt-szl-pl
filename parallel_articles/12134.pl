Oklahoma
Oklahoma ( ) – stan w Stanach Zjednoczonych leżący w środku południowej części kraju .
Graniczy od północy ze stanami Kansas i Kolorado , od wschodu ze stanami Missouri i Arkansas , od południa z Teksasem , od zachodu z Nowym Meksykiem .
Na terenie stanu dominują rozległe , równinne wyżyny .
Równina wznosi się w kierunku zachodnim .
Na wschodzie występują niewysokie pasma górskie , znane jako Kraina Ozark , z wyodrębnionym klimatem , także kulturowym .
Oklahoma zajmuje wysokie miejsce w kraju pod względem wartości produkcji paliw kopalnych , które obejmują ropę naftową i gaz ziemny .
Geografia .
Większość Oklahomy obejmują otwarte równiny i niskie wzgórza prerii .
Wiele rzek , które przepływają przez Oklahomę , zostało spiętrzonych w celu utworzenia jezior , a stan ma więcej sztucznych jezior niż jakikolwiek inny stan w kraju .
Duże obszary leśne , takie jak góry Ouachita , są typowe dla wschodniej części stanu .
Demografia .
Spis ludności z roku 2020 stwierdza , że stan Oklahoma liczy 3 959 353 mieszkańców , co oznacza wzrost o 208 002 ( 5,5 % ) w porównaniu z poprzednim spisem z roku 2010 .
Dzieci poniżej piątego roku życia stanowią 6,5 % populacji , 24,1 % mieszkańców nie ukończyło jeszcze osiemnastego roku życia , a 16,1 % to osoby mające 65 i więcej lat .
50,5 % ludności stanu stanowią kobiety .
Język .
W 2010 roku najpowszechniej używanymi językami są :
Rasy i pochodzenie .
Według danych z 2019 roku 72,4 % mieszkańców stanowi ludność biała ( 64,9 % nie licząc Latynosów ) , 8,0 % to rdzenna ludność Ameryki , 7,6 % ma rasę mieszaną , 7,3 % to czarni Amerykanie lub Afroamerykanie , 2,3 % to Azjaci , 0,12 % to Hawajczycy i mieszkańcy innych wysp Pacyfiku .
Latynosi stanowią 11,1 % ludności stanu .
Do największych grup należą osoby pochodzenia niemieckiego ( 11,4 % ) , irlandzkiego ( 9,3 % ) , meksykańskiego ( 9,0 % ) , „ amerykańskiego ” ( 7,9 % ) , angielskiego ( 7,2 % ) i afroamerykańskiego .
