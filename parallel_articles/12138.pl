Okniny-Podzdrój
Okniny-Podzdrój – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wieś licząca około 60 mieszkańców ( 24.02.2007 ) .
Przez wieś przebiega droga powiatowa Wiśniew – Zbuczyn .
