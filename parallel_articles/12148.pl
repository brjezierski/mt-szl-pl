Olkusz
Olkusz – miasto w województwie małopolskim , siedziba powiatu olkuskiego i gminy Olkusz .
Jest położony w historycznym regionie małopolskiej ziemi krakowskiej na wschodnich rubieżach Wyżyny Krakowsko-Częstochowskiej nad rzeką Babą , przy drodze krajowej nr 94 i przy drogach wojewódzkich : nr 783 i nr 791 .
Był miastem królewskim Korony Królestwa Polskiego .
Zgodnie z danymi GUS na dzień 31 grudnia 2019 r. miasto zamieszkiwało 35 170 stałych mieszkańców .
Położenie .
Olkusz leży w dawnej ziemi krakowskiej historycznej Małopolski .
Miasto położone jest na Wyżynie Krakowsko-Częstochowskiej , dokładniej na Wyżynie Olkuskiej , 6 km na południe od Pustyni Błędowskiej .
Znajduje się w połowie drogi krajowej numer 94 , pomiędzy Będzinem a Krakowem , przy linii kolejowej 62 Tunel – Sosnowiec Główny .
