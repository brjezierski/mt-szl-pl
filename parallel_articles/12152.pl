Podział administracyjny .
Olsztyn podzielony jest na 23 osiedla – jednostki pomocnicze gminy .
Stanowią one najniższy , pomocniczy , szczebel samorządu miejskiego .
Do zakresu ich działania należą sprawy publiczne o zasięgu lokalnym .
