Olsztyn
Olsztyn ( , ) – miasto na prawach powiatu , stolica województwa warmińsko-mazurskiego , siedziba władz ziemskiego powiatu olsztyńskiego , kurii archidiecezji warmińskiej i greckokatolickiej eparchii olsztyńsko-gdańskiej .
Główny ośrodek gospodarczy , edukacyjny i kulturowy , siedziba władz i instytucji regionu , a także węzeł kolejowy i drogowy .
Miasto centralne aglomeracji olsztyńskiej .
Największe miasto na Warmii .
Według danych GUS z 31 grudnia 2021 r. , miasto było zamieszkiwane przez 170 622 osoby .
Położenie .
Olsztyn położony jest w środkowej części województwa warmińsko-mazurskiego nad rzeką Łyną , w granicach Pojezierza Olsztyńskiego , które wchodzi w skład makroregionu Pojezierza Mazurskiego i jest podprowincją Pojezierzy Wschodniobałtyckich .
Od południowej strony miasto sąsiaduje z Puszczą Napiwodzko-Ramucką .
Pod względem historycznym Olsztyn leży na Warmii , w zachodniej części dawnego pruskiego terytorium plemiennego Galindii .
Miasto sąsiaduje z gminami : Dywity , Barczewo , Purda , Stawiguda , Gietrzwałd oraz Jonkowo .
Jedynym powiatem , z którym graniczy Olsztyn , jest powiat olsztyński ( ziemski ) .
Olsztyn leży 87 km od granicy z Rosją , 131 km od Kaliningradu i 408 km od stolicy Litwy – Wilna .
