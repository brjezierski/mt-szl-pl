Olsztynek ( gmina )
Olsztynek – gmina miejsko-wiejska w województwie warmińsko-mazurskim , w powiecie olsztyńskim .
W latach 1975–1998 gmina położona była w województwie olsztyńskim .
Siedziba gminy to Olsztynek .
Według Narodowego Spisu Powszechnego 31 marca 2011 gminę zamieszkiwało 13 947 mieszkańców , z czego 7725 w mieście , a 6222 – na obszarach wiejskich gminy .
Natomiast według danych z 31 grudnia 2019 roku gminę zamieszkiwało 13 701 osób .
