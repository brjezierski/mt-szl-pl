Olszyc-Folwark
Olszyc-Folwark – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Domanice .
Wierni Kościoła rzymskokatolickiego należą do parafii Nawiedzenia Najświętszej Maryi Panny w Domanicach .
W latach 1975–1998 miejscowość położona była w województwie siedleckim .
