Olza ( rzeka )
Olza ( cz .
`` Olše '' , niem .
`` Olsa '' ) – rzeka na Śląsku Cieszyńskim , na granicy Polski i Czech , będąca prawym dopływem Odry .
Przebieg .
Długość 86,2 km , powierzchnia zlewiska 1117,6 km² , z tego 479 km² na terytorium Polski .
49 większych dopływów .
Źródła w Koniakowie w gminie Istebna , pod Gańczorką i Karolówką , na zachodnich stokach grzbietu Beskidu Śląskiego , na wysokości 840–880 m n.p.m .
Uchodzi do Odry na północ od Bogumina , koło wsi Olza , na wysokości 191 m n.p.m .
Średni spadek 0,67 % , średni przepływ przy ujściu ok. 10 m³/s .
Pierwsze 16 km biegu na terenie Istebnej , w granicach Polski .
W górnym biegu w granicach Czech , od Jabłonkowa po Trzyniec Olza tworzy granicę między Beskidem Śląskim a Beskidem Śląsko-Morawskim .
