Opatowice ( Wrocław )
Opatowice ( niem .
`` Ottwitz '' ) – osiedle o charakterze wiejskim we wschodniej części Wrocławia , na lewym brzegu Odry , zaliczane do dzielnicy Krzyki .
W średniowieczu była to wieś książęca i nosiła nazwę Odra , a od 1149 w związku z nadaniem jej opactwu św .
Wincentego na Ołbinie ( dziś znajduje się na tym miejscu kościół św .
Michała ) – nazwa wsi uległa zmianie na Opatowice i taka nazwa utrzymała się do dziś .
