Opatówko
Opatówko – wieś w Polsce położona w województwie wielkopolskim , w powiecie wrzesińskim , w gminie Nekla .
Wieś duchowna , własność kapituły katedralnej gnieźnieńskiej , pod koniec XVI wieku leżała w powiecie gnieźnieńskim województwa kaliskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
We wsi znajduje się zabytkowy , drewniany kościół św .
Katarzyny Aleksandryjskiej .
Ponadto istnieje zespół dworski : dwór z przełomu XIX i XX wieku , całkowicie przebudowany na przełomie lat 70. i 80 .
XX wieku , stajnia , stodoła , chlewnia i obora z 4 ćwierci XIX wieku , tudzież pozostałości parku dworskiego .
Dom nr 2 we wsi pochodzi 4 ćwierci XIX wieku i także podlega ochronie , mimo częściowej przebudowy .
Przez miejscowość przebiega Szlak Osadnictwa Olęderskiego w Gminie Nekla .
