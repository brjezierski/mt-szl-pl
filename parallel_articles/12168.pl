Opera ( zespół muzyczny )
Opera – polski zespół rockowy utworzony w połowie 1986 roku przez byłych muzyków zespołu Republika .
Gdy w połowie czerwca 1986 roku rozpadł się zespół muzyczny Republika Grzegorz Ciechowski uruchomił swój solowy projekt pod nazwą Obywatel G.C. , a pozostali członkowie grupy , tj .
gitarzysta Zbigniew Krzywański , basista Paweł Kuczyński i perkusista Sławomir Ciesielski postanowili nadal grać razem pod nazwą Opera .
Wokalistą został Sławomir Ciesielski , na krótko zastąpiony przez Mariusza Lubomskiego .
Nieco później do grupy dołączył klawiszowiec Jacek Rodziewicz .
W 1987 roku do zespołu dołączył Robert Gawliński .
W tym składzie muzycy zagrali kilka koncertów – m.in .
w Hali Olivii i na festiwalu w Jarocinie , a także zarejestrowali nagrania na debiutancką płytę .
W nagraniu płyty uczestniczyli również saksofonista Włodzimierz Kiniorski oraz chórzystki Beata Sawicka i Lucyna Łączyńska .
Materiał ostatecznie nie został wydany , ze względu na długotrwały proces produkcji .
Zespół został rozwiązany w 1990 roku w wyniku sporów pomiędzy Gawlińskim a resztą zespołu .
Nagrania zostały ostatecznie wydane w 1993 pod nazwą `` Gawliński/Opera 1987-1988 '' , w związku z sukcesem komercyjnym zespołu Wilki któremu przewodził Robert Gawliński .
W 1993 roku Gawliński przyznał , że nie podobał mu się materiał studyjny .
Po rozpadzie zespołu Zbigniew Krzywański i Sławomir Ciesielski porozumieli się z Grzegorzem Ciechowskim i w trójkę reaktywowali Republikę .
W 2013 roku ukazała się reedycja albumu `` Gawliński i Opera 1987-1988 '' , wydana na nośniku CD .
