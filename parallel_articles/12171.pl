Opoczno ( stacja kolejowa )
Opoczno – stacja kolejowa w Opocznie , w województwie łódzkim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca regionalnego .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
Od 1 sierpnia 2009 roku do 9 grudnia 2012 roku ruch pasażerski był całkowicie zawieszony .
Od 9 grudnia 2012 roku uruchomiono jedynie pociągi pasażerskie w stronę Tomaszowa Mazowieckiego prowadzone przestarzałymi spalinowymi zestawami trakcyjnymi serii SN81 , co skutkowało częstymi odwołaniami pociągów z powodu awarii pojazdów .
Od grudnia 2013 roku uruchomiono pociągi , które jeżdżą bezpośrednio do Koluszek oraz zmieniono tabor jeżdżący na linii .
Od tej chwili jeżdżą tylko SA135 .
Od 1 lutego 2014 roku zwiększyła się ilość połączeń z Opoczna , a także pojawiły się bezpośrednie pociągi z Łodzi Kaliskiej .
W stronę Końskich i Skarżyska-Kamiennej ruch pociągów pasażerskich został wznowiony 12 grudnia 2021 roku po 12 latach przerwy .
Ze stacji odchodzi bocznica do zakładu płytek ceramicznych `` Opoczno '' .
