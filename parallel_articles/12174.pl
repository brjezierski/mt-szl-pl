Śląsk Opolski ( czasopismo )
Śląsk Opolski – czasopismo naukowe wydawane przez Instytut Śląski w Opolu w latach 1991–2010 .
Pierwszy numer ukazał się w 1991 z podtytułem : „ Społeczeństwo , gospodarka , kultura .
Kwartalnik naukowo-dokumentacyjny ” .
Od numeru 3 z 2001 podtytuł zmienił brzmienie na : „ środowisko , społeczeństwo , gospodarka , kultura ” .
Wydawanie czasopisma zakończono na numerze 4 z 2010 ( 73 ogólnego zbioru ) .
Redaktorem naczelnym był Wiesław Drobek .
