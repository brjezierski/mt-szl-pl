Animal Crossing
Animal Crossing – seria gier komputerowych należących do gatunku symulatorów życia , zapoczątkowana w 2001 roku przez Katsuyę Eguchi oraz Hisashiego Nogami .
Gracz wciela się w człowieka mieszkającego w wiosce wraz z antropomorficznymi zwierzętami .
Cechą charakterystyczną cyklu jest wykorzystywanie wewnętrznego zegara konsoli do symulacji upływu czasu .
Wszystkie części zostały wydane na konsole przedsiębiorstwa Nintendo ( wyjątkiem jest gra `` z 2017 roku , która została wydana na urządzenia mobilne ) .
