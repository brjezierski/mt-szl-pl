Orzysz ( jezioro )
Orzysz ( ) – jezioro położone na Pojezierzu Mazurskim , w Krainie Wielkich Jezior Mazurskich , w województwie warmińsko-mazurskim , w powiecie piskim , w gminie Orzysz .
Na południowo-zachodnim brzegu jeziora leży miasto Orzysz .
Jezioro typu rynnowego o powierzchni 1076 ha , położone na wysokości 117 m n.p.m. , o zróżnicowanej głębokości i na ogół zalesionych brzegach , ciągnące się z południowego zachodu na północny wschód .
Linia brzegowa o nieregularnym kształcie , charakteryzuje się licznymi zatokami o różnej wielkości oraz długimi półwyspami o wąskich cyplach , głęboko wrzynającymi się w jezioro .
W części południowej jeziora znajduje się wydłużona zatoka oddzielona długim i krętym półwyspem Ameryka ( patrz poniżej ) od sąsiedniego Jeziora Wierzbińskiego ( 70 ha ) .
Jezioro posiada 10 wysp o łącznej powierzchni ok. 74 ha .
Największa z nich to `` Różany Ostrów '' zwana również `` Wyspą Róż '' ( 54 ha ) – połączona mostem z lądem .
Jezioro łączy się z pobliskimi zbiornikami za pomocą wielu strug , cieków i kanalików .
Z jeziora wypływa rzeka Orzysza , łącząca akwen z położonymi niżej jeziorami Tyrkło i Śniardwy .
Biegnie tędy szlak kajakowy .
Cechą charakterystyczną tego akwenu jest duża zmienność głębokości oraz bogata roślinność , do brzegów przylegają przeważnie lasy sosnowe i świerkowe .
Gatunki ryb : sielawa , leszcz , okoń , szczupak , sandacz , krąp , wzdręga , płoć , ukleja .
Półwysep Ameryka .
Półwysep Ameryka leży między jeziorami Orzysz i Wierzbińskim , zwanym niekiedy Zatoką Wierzbińską .
Od strony zachodniej obydwa jeziora oddziela wąski przesmyk zwieńczony mostkiem .
Szerokość półwyspu wynosi od ok. 100 do 300 m. W najwęższych prześwietleniach widoczne są oba akweny jednocześnie .
Nad brzegiem obu jezior rosną cieniste zarośla z olszą czarną .
Korzenie niektórych drzew , podmywane falą wystają znacznie ponad powierzchnie gruntu , tworząc osobliwą formę przestrzenną .
Oba jeziora mają postać eutroficzną i podobny skład zbiorowisk przywodnych .
Występuje tu roślinność bagienna , taka jak : karbieniec pospolity , psianka słodkogórz , mięta nadwodna , grążele żółte , moczarka kanadyjska .
W kilku miejscach brzeg jest piaszczysty i łatwo dostępny .
Występują tu także rośliny synantropijne : łopiany , serdecznik pospolity , pokrzywa zwyczajna , trybula leśna , glistnik jaskółcze ziele , bylica pospolita .
Dalej na wschód a następnie na południe , półwysep staje się coraz szerszy a roślinność bujniejsza .
Pojawiają się borówki , jeżyny , mchy , a jesienią grzyby .
W lesie napotkać można okopy z czasów II wojny światowej .
Wzdłuż Jeziora Wierzbińskiego , na jego południowym brzegu rozciąga się wieś Wierzbiny .
