Orłowa ( góra )
Orłowa ( 813 m n.p.m . )
– góra w Beskidzie Śląskim , w północnej części Pasma Równicy .
Opis .
Orłowa jest rozległą górą , oddzieloną od samej Równicy głęboko wciętą przełęczą Beskidek ( 668 m n.p.m . ) .
Ma kształt potężnego rogalika zwróconego rogami ku południu .
Ramię wschodnie i środek owego rogalika leżą w głównym grzbiecie pasma , zaś ramię zachodnie , rozszerzające się na końcu , opada ku dolinom Dobki i Suchej Dobki .
Kulminacja znajduje się w środku owego rogalika , w grzbiecie głównym pasma .
Wzdłuż wewnętrznego łuku sierpowatego grzbietu ciągną się widokowe polany z rozrzuconymi zabudowaniami osiedla Orłowa .
