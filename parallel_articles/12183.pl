Osnowo
Osnowo – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie chełmińskim , w gminie Chełmno .
W latach 1975–1998 miejscowość należała administracyjnie do województwa toruńskiego .
Demografia .
W roku 2006 Osnowo zamieszkiwało 90 osób .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 94 mieszkańców .
Jest najmniejszą miejscowością gminy Chełmno .
