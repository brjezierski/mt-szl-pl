Ostrołęka
Ostrołęka – miasto na prawach powiatu w województwie mazowieckim .
Leży nad rzeką Narew .
Siedziba powiatu ostrołęckiego .
W latach 1975–1998 stolica województwa ostrołęckiego .
Ośrodek usług oraz przemysłu energetycznego , budowlanego , celulozowo-papierniczego i spożywczego .
Ośrodek folkloru kurpiowskiego .
Miasto królewskie Korony Królestwa Polskiego w województwie mazowieckim .
Pierwsze pod względem wielkości miasto dawnej ziemi łomżyńskiej historycznego Mazowsza .
Położenie .
Ostrołęka jest położona w północno-wschodniej Polsce w Nizinie Północnomazowieckiej , na skraju Puszczy Zielonej , w środkowej części powiatu ostrołęckiego , w województwie mazowieckim .
Przez miasto przepływają trzy rzeki : Narew , Omulew i Czeczotka .
Nazwa miasta ściśle związana jest z topograficznymi właściwościami terenu ( „ ostre ” łąki nad rzeką , które wiosną zalewa woda ) .
