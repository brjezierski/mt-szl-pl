Ostrożne
Ostrożne – wieś w Polsce położona w województwie podlaskim , w powiecie zambrowskim , w gminie Szumowo .
Wierni kościoła rzymskokatolickiego należą do parafii Najświętszej Maryi Panny Częstochowskiej w Szumowie .
Historia .
W latach 1921 – 1939 wieś leżała w województwie białostockim , w powiecie łomżyńskim , w gminie Szumowo .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 347 osób w 53 budynkach mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w Szumowie .
Podlegała pod Sąd Grodzki w Zambrowie i Okręgowy w Łomży ; właściwy urząd pocztowy mieścił się w Szumowie .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
