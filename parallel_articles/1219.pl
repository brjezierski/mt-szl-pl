Animal Liberation Front
Animal Liberation Front ( ALF , dosł .
`` Front Wyzwolenia Zwierząt '' ) – międzynarodowa organizacja walcząca o prawa zwierząt , stosująca akcję bezpośrednią w ramach niekierowanego oporu .
Uznawana jest przez rząd USA za terrorystyczną .
Nie należy wiązać jej działań z polskim Frontem Wyzwolenia Zwierząt .
Animal Liberation Front jest powstałym w 1976 roku ogólnoświatowym ruchem radykalnych obrońców zwierząt .
Jego aktywiści prowadzą zakonspirowane akcje bezpośrednie na rzecz zwierząt .
Akcje te mają na celu albo natychmiastowe uwolnienie zwierząt z miejsc , w których , zdaniem ALF , dzieje im się krzywda .
Albo radykalne działanie na szkodę instytucji powodujących , zdaniem ALF , to cierpienie .
Stąd pochodzą akcje uwalniania zwierząt z ferm futrzarskich i hodowlanych ( według ALF nie dochodzi tu do kradzieży , ponieważ uważa się zwierzęta za istoty wolne ) oraz niszczenie mienia laboratorium wiwisekcyjnych , rzeźni itp .
( pojmowanego przez ALF jako akcję uzasadnionej samoobrony współodczuwających ) .
Według Departamentu Bezpieczeństwa Krajowego USA organizacja ALF jest odpowiedzialna za 39 % ataków ekoterrorystycznych w Stanach Zjednoczonych .
