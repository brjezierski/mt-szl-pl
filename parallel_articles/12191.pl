Ostróda ( stacja kolejowa )
Ostróda – stacja kolejowa w Ostródzie , w województwie warmińsko-mazurskim , w Polsce .
Opis .
Posiada przejście podziemne łączące perony z budynkiem dworca , semafory kształtowe oraz bocznice kolejowe do Zakładów Naprawczych Taboru Kolejowego pod zarządem Wagon Service Ostróda .
