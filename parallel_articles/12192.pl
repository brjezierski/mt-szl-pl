Przed 1945 rokiem nad Duńczycą istniało kąpielisko Waldowshof oraz siedziba sportowy klubu pływackiego Waspo Stettin .
W ciągu kilkunastu lat po II wojnie światowej osiedliło się tu kilkanaście rodzin pochodzących głównie z Kresów .
Pracowali w porcie i stoczniach , ale też trudnili się rolnictwem i rybołówstwem .
W okresie przed usypaniem grobli na stały ląd można było się dostać jedynie łódką lub lodem w okresie zimowym .
W latach siedemdziesiątych XX w. dyrekcja portu zadecydowała o zajęciu wyspy na potrzeby planowanej bazy kontenerowej ( nigdy nie powstałej na tym terenie , pomijając budowę nabrzeża fińskiego ) .
Mieszkańcom przydzielono mieszkania w blokach z wielkiej płyty .
W 1998 wyspę zamieszkiwały dwie kobiety .
