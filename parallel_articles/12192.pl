Ostrów Grabowski
Ostrów Grabowski ( do 1945 , od 1945 `` Wyspa Zielona '' , współcześnie jedynie w wydawnictwach wojskowych określana także jako `` Widzka Kępa '' ) – dawna wyspa rzeczna na szczecińskim Międzyodrzu , powstała w procesie podziału wyspy Fette Ort .
Obecnie połączona jest szeroką groblą z sąsiednią Łasztownią .
Teren opływają wody powstałych w XX w. kanałów : Dębickiego , Grabowskiego ( zastąpił on istniejący wcześniej ciek Grabower Graben ) , Przekopu Mieleńskiego , oraz rzeki Duńczycy .
Obecna nazwa polska urzędowo wprowadzono została rozporządzeniem w 1949 roku , zastępując poprzednią nazwę niemiecką .
Zagospodarowanie .
W północnej części wyspy znajduje się przeznaczony do likwidacji rodzinny ogród działkowy „ Ostrówek ” .
W części południowej działa oczyszczalnia ścieków `` Ostrów Grabowski '' i Zakład Termicznego Unieszkodliwiania Odpadów ( spalarnia śmieci ) .
Na wyspie zlokalizowane są także m.in .
