Ostrówek ( wzgórze )
Ostrówek ( 340 m ) – wzgórze w miejscowości Olsztyn na Wyżynie Krakowsko-Częstochowskiej .
Znajduje się w niezabudowanym terenie , wśród pól w zachodniej części miasta .
Na jego grzbiecie znajduje się kilka niewielkich wapiennych skał .
Jest w większości bezleśne , dzięki czemu jest dobrym punktem widokowym .
