Ozorków
Ozorków – miasto nad rzeką Bzurą , w województwie łódzkim , w powiecie zgierskim .
Miasto znajduje się 20 km na północ od Łodzi .
W latach 1975–1998 miasto administracyjnie należało do tzw .
„ małego ” województwa łódzkiego .
Według danych GUS z 31 grudnia 2019 r. miasto miało 19 311 mieszkańców .
Położenie .
Ozorków leży w historycznej ziemi łęczyckiej .
Według danych z roku 2007 Ozorków zajmuje obszar 1546 ha , czyli 15,46 km² .
