Urocystis anemones
Urocystis anemones ( Pers . )
G. Winter – gatunek podstawczaków należący do rodziny `` Urocystidaceae '' .
Grzyb mikroskopijny , pasożyt niektórych roślin z rodziny jaskrowatych ( `` Ranunculaceae '' ) , zwłaszcza zawilca gajowego ( `` Anemone nemorosa '' ) i jaskra rozłogowego ( `` Ranunculus repens '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Urocystis , Urocystidaceae , Urocystidales , Incertae sedis , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1797 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo anemones '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu H.G .
Winter w 1880 r .
Synonimy :
Morfologia .
Na liściach , ogonkach liściowych i na łodygach porażonych roślin tworzy kupki zarodni w postaci nabrzmiałych pęcherzy , początkowo pomarańczowych , potem w miarę dojrzewania zarodników coraz ciemniejszych – pomarańczowobrązowych i czarnych .
