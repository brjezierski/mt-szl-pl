Urocystis cepulae
Urocystis cepulae Frost – gatunek podstawczaków należący do rodziny `` Urocystidaceae '' .
Grzyb mikroskopijny , pasożyt cebuli , wywołujący u niej chorobę o nazwie głownia cebuli .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Urocystis , Urocystidaceae , Urocystidales , Incertae sedis , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1877 r. Charles Christopher Frost .
Synonimy :
Morfologia i rozwój .
`` Urocystis cepulae '' pod skórką porażonych roślin tworzy wydłużone , ciemnobrązowe , niemal czarne skupiska teliospor .
Pojedyncza teliospora ma kształt od kulistego do elipsoidalnego , średnicę 11–14 µm , czerwonawo-brązowy kolor i gładką ścianę .
Otoczona jest warstwą lekko zabarwionych sterylnych komórek o średnicy 4–6 µm i wraz z tą warstwą ma średnicę 14–22 µm .
Kiełkujące teliospory tworzą strzępki wrastające do siewek cebuli jeszcze przed ich wschodem .
