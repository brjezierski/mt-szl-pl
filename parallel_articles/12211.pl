Urocystis colchici
Urocystis colchici ( Schltdl . )
Rabenh .
– gatunek podstawczaków należący do rodziny `` Urocystidaceae '' .
Grzyb mikroskopijny , pasożyt zimowitów , wywołujący u nich chorobę o nazwie głownia zimowita .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Urocystis , Urocystidaceae , Urocystidales , Incertae sedis , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1826 r. Diederich Franz Leonhard von Schlechtendal nadając mu nazwę `` Caeoma colchici '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Gottlob Rabenhorst w 1861 r .
Morfologia i tryb życia .
`` Urocystis colchici '' jest obligatoryjnym pasożytem zimowitów .
Poraża je systemowo , zimuje w ich bulwach .
