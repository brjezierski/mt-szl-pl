Urocystis fischeri
Urocystis fischeri Körn .
– gatunek podstawczaków należący do rodziny `` Urocystidaceae '' .
Grzyb mikroskopijny pasożytujący na turzycach .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Urocystis , Urocystidaceae , Urocystidales , Incertae sedis , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1879 r. Friedrich August Körnicke .
Synonimy :
Charakterystyka .
Pasożytuje na licznych gatunkach roślin z rodziny ciborowatych ( `` Cyperaceae '' ) .
Objawami porażenia są szaroołowiowe , podłużne plamy na liściach , rzadziej na źdźbłach .
Powstają w nich zarodniki patogenu .
Po ich dojrzeniu skórka liści pęka i uwalniają się ciemnobrązowe , pudrowe masy zlepionych po 1–3 ( 4 ) zarodników , całkowicie owiniętych w płaszcz z mniejszych sterylnych komórek .
