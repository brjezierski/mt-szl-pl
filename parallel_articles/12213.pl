Urocystis occulta ( Wallr . )
A.A. Fisch .
Waldh .
– gatunek podstawczaków należący do rodziny `` Urocystidaceae '' .
Grzyb mikroskopijny , pasożyt , powodujący u żyta chorobę o nazwie głownia źdźbłowa żyta .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Urocystis , Urocystidaceae , Urocystidales , Incertae sedis , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1833 r. CF.W .
Wallroth nadając mu nazwę `` Erysibe occulta '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu A.A. Fisch .
Waldh .
