Urocystis violae
Urocystis violae ( Sowerby ) A.A. Fisch .
Waldh .
– gatunek podstawczaków należący do rodziny `` Urocystidaceae '' .
Grzyb mikroskopijny pasożytujący na fiołkach ( `` Viola '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Urocystis , Urocystidaceae , Urocystidales , Incertae sedis , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1815 r. James Sowerby nadając mu nazwę `` Granularia violae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Alexandr Alexandrowitch Fischer von Waldheim w 1867 r .
Synonimy :
Charakterystyka .
Na ogonkach liściowych , a także innych częściach rośliny , powoduje powstawanie kilkucentymetrowych pęcherzy zawierających czarno-brązową masę ustilospor .
Są zlepione w grupki liczące po 3-20 zarodników owiniętych ciągłą warstwą żółtawych sterylnych komórek .
Występuje w Azji i Europie , zarówno na dziko rosnących , jak i uprawianych gatunkach fiołków .
Patogen trwale zadamawia się w korzeniach porażonych roślin .
Nowe infekcje są inicjowane przez ustilospory , które przeżywają w rozkładających się częściach roślin i glebie .
Odnotowano infekcje także przez nasiona , ale przypuszczalnie zdarza się to rzadko .
