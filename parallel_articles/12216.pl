Uromyces acetosae
Uromyces acetosae J. Schröt .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Pasożyt roślin z rodzaju szczaw ( `` Rumex '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonim : `` Coeomurus acetosae '' ( J .
Schröt . )
Kuntze :
Morfologia i rozwój .
Rdza jednodomowa , tzn. , że cały jej cykl życiowy odbywa się na jednym żywicielu ( szczaw ) .
Rdza pełnocyklowa , wytwarzająca wszystkie typowe dla rdzowców rodzaje owocników i zarodników .
Na górnej stronie porażonych liści powoduje powstawanie czerwonych lub purpurowych plam .
Spermogonia na górnej stronie blaszki .
Ecja , uredinia i telia powstają na dolnej stronie liści .
Ecja białożółte , kubkowate , w obrębie plam .
Uredinia jasnobrązowe , powstające w nich urediniospory o powierzchni drobnokolczastej , z trzema porami rostkowymi w okolicach równika .
Telia ciemnobrązowe , teliospory jednokomórkowe , z brodawkami ułożonymi w linie .
Występowanie .
W Europie jest szeroko rozprzestrzeniony .
Poza Europą znane są jego stanowiska tylko w Maroku w Afryce Północnej .
Monofag występujący na niektórych gatunkach szczawiu : `` Rumex acetosa '' , `` Rumex acetosella '' , `` Rumex alpestris '' , `` Rumex alpinus '' , `` Rumex thyrsiflorus '' .
W Polsce do 2003 r. opisano jego występowanie tylko na `` R. acetosa '' .
