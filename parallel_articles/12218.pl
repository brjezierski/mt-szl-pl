Uromyces beticola
Uromyces beticola ( Bellynck ) Boerema , Loer .
& amp ; Hamers – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten w 1801 r. opisał Auguste Alexis Adolphe Alexandre Bellynck , nadając mu nazwę `` Uredo betae '' .
Obecną , uznaną przez Index Fungorum nazwę , nadali mu w 1987 r. Gerhard H. Boerema , W.M .
Loreakker i Maria E.C .
Hamers .
Ważniejszy synonim : `` Uromyces betae '' ( Pers . )
J. Kickx f. 1867 .
Morfologia i rozwój .
`` Uromyces beticola '' jest pasożytem jednodomowym , tzn .
że jego pełny cykl życiowy odbywa się na jednym żywicielu .
Jest też pełnocyklowy , tzn , że wytwarza wszystkie rodzaje zarodników typowe dla rodziny rdzowatych .
Ecjospory są niemal kuliste , o wymiarach 17–25 × 15–18 μm , urediniospory również niemal kuliste , lecz większe , o wymiarach 26–35 × 20–24 μm .
Teliospory są owalne , mają grubszą ścianę i wymiary 22–34 × 16–26 μm .
Na ich szczycie jest charakterystyczne , stożkowe wieczko .
Teliospory podobnie jak u innych przedstawicieli rodziny rdzowatych , są przetrwalnikami .
Zimują na resztkach buraków w glebie .
Wiosną rozwijają się z nich bazydiospory będące źródłem infekcji pierwotnej .
Porażają buraki powodując u nich chorobę o nazwie rdza buraka .
