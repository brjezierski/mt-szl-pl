Uromyces dactylidis
Uromyces dactylidis G.H .
Otth – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Pasożyt roślin z rodzaju jaskier , grzebienica ( `` Cynosurus '' ) i kupkówka .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimów ma ponad 20 .
Morfologia i rozwój .
Rdza dwudomowa , tzn .
że jej cykl życiowy odbywa się na dwóch żywicielach .
Rdza pełnocyklowa , wytwarzająca wszystkie typowe dla rdzowców rodzaje owocników i zarodników .
Spermogonia i ecja powstają na jaskrach ( żywiciel ecjalny ) , uredinia i telia na niektórych gatunkach roślin z rodziny wiechlinowatych ( żywiciele telialni ) .
Ecidia powstają głównie na dolnej stronie liści .
Żółtobrązowe uredinia mają długość 0,2-2 mm i powstają na górnej powierzchni liści .
Brak parafiz .
