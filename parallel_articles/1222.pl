Ankara
Ankara ( wcześniej `` Angora '' , łac .
`` Ancyra '' , gr .
`` Ἄγκυρα '' , ‘ kotwica ’ ) – miasto w środkowej Turcji , będące jej stolicą od 1923 roku .
Jest po Stambule drugim co do wielkości miastem Turcji .
Położone na wysokości 938 m n.p.m .
Leżąca w środkowej Anatolii Ankara jest ważnym ośrodkiem handlowym i przemysłowym , siedzibą rządu państwa oraz wszystkich ambasad .
W przeszłości znana była z hodowli kóz angora , kotów angora oraz białych królików angora , gruszek , miodu i winogron .
Miasto nad rzeką Ankara Çayı , będącą dopływem rzeki Sakarya , usytuowane jest w najsuchszym rejonie Turcji i otoczone jałowymi stepami .
