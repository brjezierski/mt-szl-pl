Uromyces ficariae
Uromyces ficariae ( Schumach . )
Lév .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Pasożyt ziarnopłonu wiosennego .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1803 r. Heinrich Christian Friedrich Schumacher nadając mu nazwę `` Uredo ficariae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Joseph-Henri Léveillé w 1860 r .
Synonimy :
Charakterystyka .
Rdza jednodomowa – cały jej cykl życiowy odbywa się na jednym żywicielu .
Rdza mikrocykliczna , wytwarzająca tylko telia .
Monofag , którego jedynym żywicielem jest ziarnopłon wiosenny ( `` Ficaria verna '' ) .
Od marca do czerwca na obydwu stronach liści , oraz na ogonkach liściowych powstają bladożółte plamy , a w ich obrębie skupiska brązowych telii .
W Europie jest szeroko rozprzestrzeniony .
Występuje także w Armenii , Azerbejdżanie , Gruzji , Iranie .
