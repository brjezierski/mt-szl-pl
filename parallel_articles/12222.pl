Uromyces pisi-sativi
Uromyces pisi-sativi ( Pers . )
Liro – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , saprotrof i pasożyt roślin .
Wywołuje chorobę zwaną rdzą grochu .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo appendiculata ß pisi-sativi '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Johan Ivar Liro w 1908 r .
Synonimów ma ok. 40 ..
Morfologia .
Pyknidia ( spermogonia ) występują zazwyczaj razem z ecjami , na liściach wilczomleczy , często pokrywając całą ich powierzchnię .
Pomarańczowoczerwone ecja mają średnicę 0,3–0,4 mm .
Powstają w nich szeroko elipsoidalne ecjospory o średnicy 17–22 μm i powierzchni gęsto pokrytej brodawkami o wysokości 1 μm .
Uredinia powstają głównie na dolnej stronie liści grochu .
