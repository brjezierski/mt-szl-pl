Uromyces viciae-fabae
Uromyces viciae-fabae ( Pers . )
J. Schröt .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , saprotrof i pasożyt roślin .
Wywołuje chorobę zwaną rdzą bobiku .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Ch.H .
Persoon nadając mu nazwę `` Uredo viciae-fabae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu J. Schröter w 1875 r .
Synonimy .
Morfologia .
Pyknidia występują zazwyczaj razem z ecjami na obydwu stronach blaszki liściowej .
Ecja mają rdzawą barwę , średnicę 3–4 mm , zazwyczaj otaczają pyknidia , lub występują w rozproszeniu .
Ecjospory sferoidalne , o średnicy 8-26 μm , pokryte brodawkami o wysokości 1 μm .
Uredinia o cynamonowej barwie rozwijają się na blaszkach i ogonkach liści .
Mają średnicę 0,5–1 mm .
