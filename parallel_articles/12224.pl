Uroševac ( cyr .
Урошевац , alb .
`` Ferizajt '' ) – gmina w Kosowie w regionie Uroševac .
Ma około 345 km² powierzchni i jest zamieszkana przez 105 116 osób ( szacunki na 2020 rok ) .
Głównym miastem jest Uroševac .
Według spisu powszechnego z 1991 roku gmina zamieszkana była przez 100 104 Albańczyków , 8 314 Serbów oraz 3 Czarnogórców .
Według spisu z 2011 roku było to 104 152 Albańczyków , 3 629 Aszkalów i 32 Serbów .
Gospodarkę gminy stanowią głównie rolnictwo oraz niewielkie firmy budowlane .
