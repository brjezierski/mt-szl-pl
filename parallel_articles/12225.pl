Ursula Konzett
Ursula Konzett ( ur .
15 listopada 1959 w Grabs w Szwajcarii ) – narciarka alpejska reprezentująca Liechtenstein , brązowa medalistka igrzysk olimpijskich i mistrzostw świata .
Kariera .
Po raz pierwszy na arenie międzynarodowej Ursula Konzett pojawiła się w sezonie 1975/1976 .
Pierwsze punkty w zawodach Pucharu Świata wywalczyła 24 marca 1977 roku w Sierra Nevada , zajmując trzecie miejsce w gigancie .
Tym samym nie tylko zdobyła pierwsze pucharowe punkty , ale od razu stanęła na podium , przegrywając jedynie z Lise-Marie Morerod ze Szwajcarii oraz Austriaczką Ingrid Eberle .
W kolejnych latach jeszcze sześciokrotnie stawała na podium , odnosząc przy tym dwa zwycięstwa : 22 stycznia 1982 roku w Lenggries oraz 3 marca 1982 roku w Waterville Valley wygrywała slalomy .
Zwycięstwo w Waterville Valley było równocześnie ostatnim pucharowym podium w jej karierze .
Najlepsze wyniki osiągnęła w sezonie 1981/1982 , kiedy zajęła szóste miejsce w klasyfikacji generalnej i trzecie w klasyfikacji giganta .
W 1976 roku wystartowała na igrzyskach olimpijskich w Innsbrucku , gdzie jej najlepszym wynikiem było jedenaste miejsce w slalomie .
Na rozgrywanych cztery lata później igrzyskach w Lake Placid wystartowała w gigancie i slalomie , jednak obu konkurencji nie ukończyła .
Wzięła także udział w igrzyskach olimpijskich w Sarajewie w 1984 roku , gdzie wywalczyła brązowy medal w slalomie .
Po pierwszym przejeździe slalomu Konzett zajmowała drugie miejsce , tracąc do prowadzącej Christelle Guignard z Francji 0,10 sekundy .
W drugim przejeździe osiągnęła szósty czas , co dało jej trzeci łączny wynik i miejsce na najniższym stopniu podium .
Ostatecznie straciła 1,03 sekundy do Włoszki Paoletty Magoni i 0,12 sekundy do Francuzki Perrine Pelen .
Na tych samych igrzyskach wystartowała także w gigancie , ale nie ukończyła rywalizacji .
W międzyczasie wywalczyła brązowy medal w gigancie podczas mistrzostw świata w Schladming w 1982 roku .
Uległa tam tylko Szwajcarce Erice Hess i Christin Cooper z USA .
W marcu 1985 roku zakończyła karierę .
Podczas ceremonii otwarcia igrzysk olimpijskich w Innsbrucku była chorążym reprezentacji Liechtensteinu .
