Urugwaj
Urugwaj ( `` Uruguay '' , Wschodnia Republika Urugwaju – `` República Oriental del Uruguay '' ) – państwo w Ameryce Południowej , nad Atlantykiem , graniczące z Argentyną od zachodu i Brazylią od północy .
Stolicą państwa jest Montevideo , port atlantycki u ujścia La Platy .
Po drugiej stronie tego estuarium leży stolica Argentyny – Buenos Aires .
Dawniej Urugwaj stanowił część Wicekrólestwa La Platy i nosił nazwę Banda Oriental ( Brzeg Wschodni ) .
