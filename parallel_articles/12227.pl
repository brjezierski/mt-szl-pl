Język urumski
Język urumski ( urum , grecko-tatarski ) – język turkijski używany przez turkojęzycznych Greków w kilku wsiach w Gruzji i na południowo-wschodniej Ukrainie .
Jest zagrożony wyginięciem .
Klasyfikacja .
Publikacja Ethnologue uznaje urum za niezależny język turkijski .
