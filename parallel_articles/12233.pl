Ustanów
Ustanów – wieś w Polsce położona w województwie mazowieckim , w powiecie piaseczyńskim , w gminie Prażmów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa warszawskiego .
Miejscowość położona jest na skraju Chojnowskiego Parku Krajobrazowego , ok. 36 km na południe od centrum Warszawy .
Szacuje się , że powstała ponad 600 lat temu ( najstarsza wzmianka o tej miejscowości pochodzi z 1468 roku ) .
