Głownia owsa
Głownia owsa ( `` Ustilago avenae '' ( Pers . )
Rostr . )
– gatunek grzybów z rodziny głowniowatych ( `` Ustilaginaceae '' ) .
Wywołuje u owsa chorobę o nazwie głownia pyląca owsa ( głownia pyłkowa owsa ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ustilago , Ustilaginaceae , Ustilaginales , Ustilaginomycetidae , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Persoon nadając mu nazwę `` Uredo segetum ?
avenae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1890 r. Emil Rostrup , przenosząc go do rodzaju `` Ustilago '' .
Niektóre synonimy :
Morfologia .
Masa zarodników puszysta , ciemno-zielonobrązowa .
Konidia kuliste lub niemal kuliste , blado zielonkawobrązowe , o powierzchni pokrytej drobniutkimi kolcami .
Mają rozmiar 5-8 μm .
Gładkie zarodniki odróżniają `` Ustilago avenae '' od pokrytych drobniutkimi kolcami zarodników głowni jęczmienia `` Ustilago nuda '' .
Występowanie i znaczenie .
`` Ustilago avenae '' wywołuje u owsa chorobę o nazwie głownia pyląca owsa .
Atakuje także inne rajgras ( `` Arrhenatherum '' ) .
