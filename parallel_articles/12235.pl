Charakterystyka .
Objawem porażenia rośliny są długie , ciemne równoległe prążki na liściach , czasem także na pochwach i źdźbłach .
Znajduje się w nich oliwkowobrązowa , pudrowa masa zarodników .
Zarodniki 3-6 µm , gładkie .
Grzyb ma charakter ogólnoustrojowy ; porażone rośliny zwykle nie kwitną .
