Ustilago filiformis
Ustilago filiformis ( Schrank ) Rostr .
– gatunek grzybów z rodziny głowniowatych ( `` Ustilaginaceae '' ) .
Grzyb mikroskopijny , monofag pasożytujący na roślinach z rodzaju manna .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ustilago , Ustilaginaceae , Ustilaginales , Ustilaginomycetidae , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1793 r. Franz de Paula von Schrank nadając mu nazwę `` Lycoperdson filiformis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1890 r. Emil Rostrup .
