Ustilago trichophora
Ustilago trichophora ( Link ) Kunze – gatunek grzybów z rodziny głowniowatych ( `` Ustilaginaceae '' ) .
Grzyb mikroskopijny , pasożytujący na chwastnicy jednostronnej i powodujący u niej grzybową chorobę o nazwie głownia chwastnicy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ustilago , Ustilaginaceae , Ustilaginales , Ustilaginomycetidae , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1825 r. Johann Heinrich Friedrich Link nadając mu nazwę `` Caeoma trichophora '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1830 r. Gustav Kunze .
Synonimy :
Morfologia .
Zarodnie rozwijają się z lokalnie zagęszczonej grzybni zlokalizowanej w zewnętrznych komórkach miąższowych żywiciela .
Mają kolor od oliwkowożółtego do żółtawobrązowego i wymiary 8–14 × 16–23 mm .
Powstają w guzkowatych naroślach o chropowatej powierzchni otaczających cały pęd żywiciela .
