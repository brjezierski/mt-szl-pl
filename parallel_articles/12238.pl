Głownia pszenicy
Głownia pszenicy ( `` Ustilago tritici '' ( Pers . )
Rostr . )
– gatunek grzybów z rodziny głowniowatych ( `` Ustilaginaceae '' ) .
Wywołuje chorobę pszenicy o nazwie głownia pyląca pszenicy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ustilago , Ustilaginaceae , Ustilaginales , Ustilaginomycetidae , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowany został w 1801 r. przez Ch.H .
Persoona jako `` Uredo segetum ß tritici '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1842 r. E. Rostrup , przenosząc go do rodzaju `` Ustilago '' .
Synonimy nazwy naukowej :
Cykl rozwojowy .
Wytwarzane przez głownię pszenicy teliospory zakażają zdrowe kłosy pszenicy .
Z kiełkujących teliospor wyrastają czterokomórkowe podstawki , nie powstają z nich jednak sporydia .
Pomiędzy dwoma haploidalnymi komórkami tej samej podstawki lub sąsiednich podstawek dochodzi do plazmogamii i powstaje dwujądrowa komórka ( dikarion ) .
Wyrasta z niej strzępka , która wrasta do zalążni zdrowego kwiatu .
Kwiat rozwija się pozornie normalnie , następuje jego zapylenie , zapłodnienie i powstaje ziarniak , jednak w jego zarodku i w okolicach tarczki zarodkowej znajdują się strzępki patogena .
Ziarno takie wygląda normalnie i optycznie nie można go odróżnić od ziarna zdrowego .
Po jego wysianiu , podczas kiełkowania ziarniaków , strzępki patogena rosną wraz z pędem rośliny i poprzez łodygę dostają się na jego wierzchołek .
W zawiązkach kłosków silnie rozrasta się grzybnia patogena niszcząc je .
Zgrubiałe strzępki grzybni rozpadają się tworząc kuliste teliospory .
Mają średnicę 5-9 μm i drobnobrodawkowatą powierzchnię .
Nieco przed kiełkowaniem teliospor zachodzi w nich kariogamia , w wyniku czego z dikarionu powstaje diploidalna zygota .
W czasie kiełkowania teliospor zachodzi w nich mejoza i znów powstają haploidalne komórki .
Charakterystyczne jest oddalenie w czasie procesów plazmogamii i kariogamii .
