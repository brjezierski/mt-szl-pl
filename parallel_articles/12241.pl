Ustroń ( sielsowiet Opsa )
Ustroń – dawna osada .
Tereny , na których była położona , leżą obecnie na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Opsa .
Historia .
W czasach zaborów leżała w granicach Imperium Rosyjskiego .
W latach 1921–1945 zaścianek a następnie osada leżała w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie brasławskim , w gminie Opsa .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 6 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 2 domach zamieszkiwało 11 osób .
Miejscowość należała do parafii rzymskokatolickiej w Opsie .
Podlegała pod Sąd Grodzki w Opsie i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Opsie .
