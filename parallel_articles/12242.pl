Utah ( jezioro )
Utah Lake – jezioro w USA , w Wielkiej Kotlinie , o powierzchni 392 km² .
Jest ono jedną z pozostałości jeziora Bonneville .
Jezioro Utah zasila , poprzez rzekę Jordan , Wielkie Jezioro Słone .
Nad jego brzegami znajduje się park stanowy .
Wody jeziora wykorzystywane są do nawadniania pól uprawnych .
Jest także używane w celach rekreacyjnych – uprawia się na nim sporty wodne , takie jak kajakarstwo , żeglarstwo , a także łowi ryby ( głównie sumokształtne , sandacza amerykańskiego , `` Morone chrysops '' i ryby z rodzaju `` Micropterus '' ) .
