Utting am Ammersee
Utting am Ammersee – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Monachium , w powiecie Landsberg am Lech .
Leży około 15 km na wschód od Landsberg am Lech , nad jeziorem Ammersee , przy linii kolejowej Augsburg – Weilheim in Oberbayern .
Polityka .
Wójtem gminy jest Josef Lutzenberger , wcześniej urząd ten obejmował Ottmar Mayr , rada gminy składa się z 16 osób .
