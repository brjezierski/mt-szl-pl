Uunartoq Qeqertoq
Uunartoq Qeqertoq – odkryta we wrześniu 2005 ( gdzie indziej : 2006 ) wyspa o kształcie podobnym do litery W , leżąca 644 km na północ od kręgu polarnego przy wschodnim wybrzeżu Grenlandii .
W języku grenlandzkim nazwa ta oznacza `` Wyspa Ocieplenia '' .
Do 2005 roku obszar ten był zaznaczany na mapach jako półwysep .
Dopiero gdy spajający dwa fragmenty lądu lodowiec wycofał się , okazało się , że jest to wyspa .
Odkrywcą jest amerykański badacz Dennis Schmitt .
