Anna Belknap
Anna Belknap ( ur .
22 maja 1972 w Damariscotta ) – amerykańska aktorka .
Uczęszczała do Middlebury College , a następnie do American Conservatory Theater , gdzie zdobyła wykształcenie aktorskie .
Tak jak jej mąż – Eric Siegel , należy ona również do `` Rude Mechanicals Theatre Company '' .
