Język uzbecki
Język uzbecki ( uzb .
`` Oʻzbekcha '' ) – język z rodziny języków tureckich , urzędowy w Uzbekistanie .
Dane mówiące o liczbie mówiących językiem uzbeckim są rozbieżne .
Zgodnie ze spisem powszechnym , przeprowadzonym w 1989 roku , językiem uzbeckim na terenie byłego ZSRR posługiwało się ok. 16,7 mln osób .
Poza Uzbekistanem uzbecki jest i był w przeszłości używany głównie w Afganistanie , gdzie liczbę jego użytkowników ocenia się na ok. 1,2–1,4 mln osób .
Uzbeckim posługują się także Uzbecy osiadli w sąsiednich republikach środkowoazjatyckich : w Kazachstanie , Kirgistanie , azjatyckiej części Rosji , w Turkmenistanie , a także w Chinach .
Stosunkowo niewielkie skupiska emigrantów uzbeckich występują w Australii , Izraelu , Niemczech , Turcji , na Ukrainie i w Stanach Zjednoczonych .
W 2019 roku w Polsce był tylko jeden aktywny tłumacz przysięgły języka uzbeckiego .
