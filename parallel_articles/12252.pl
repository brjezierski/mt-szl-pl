Historia .
W 50 roku Rzymianie wybudowali w tych okolicach 50-kilometrowy akwedukt , który prowadził wodę z rzeki Eure do miasta Nîmes .
Jego pozostałości zwane Pont du Gard są popularnym wśród turystów celem wycieczek .
W tamtym okresie powstało również obwarowane osiedle obronne , które uważa się za początki Uzès .
