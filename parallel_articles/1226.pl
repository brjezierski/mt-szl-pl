Anna Louisa Karsch
Anna Louisa Karsch , znana jako Die Karschin ( ur .
1 grudnia 1722 prawdopodobnie w Przetocznicy , zm .
12 października 1791 w Berlinie ) – poetka okresu oświecenia , nazywana `` niemiecką Safoną '' lub `` zmartwychwstałą Safoną '' , pierwsza Niemka , która utrzymywała się z twórczości literackiej i pierwsza obywatelka Prus , która wzięła rozwód .
Życiorys .
Przyszła na świat na farmie mlecznej jako córka karczmarza Christiana Dürbacha .
Po śmierci ojca ( 1728 ) matka oddała ją pod opiekę stryja lub stryjecznego dziadka , Martina Fetke , urzędnika sądowego w Trzcielu .
Zapewnił jej edukację , nauczył czytać i pisać po łacinie i niemiecku oraz liczyć .
Po jakimś czasie matka zabrała ją do domu , gdzie pracowała w gospodarstwie , pilnowała przyrodniego rodzeństwa w wieku 13–16 lat i pracowała jako służąca w innych domach .
Czytała m.in .
literaturę religijną , którą otrzymała od pasterza bydła .
W 1738 wyszła za mąż za sukiennika Michaela Hiersekorna ze Świebodzina .
Urodziła dwoje dzieci .
Mąż był alkoholikiem i nie akceptował zainteresowania żony poezją i książkami , które dawały jej ukojenie w codziennym życiu .
W 1749 rozwiodła się jako pierwsza kobieta w Prusach .
Była wówczas w trzeciej ciąży .
Jej drugim mężem był krawiec Daniel Karsch ze Wschowy .
Przeprowadzili się do centrum Polski , później wrócili do Wschowy .
Talent poetycki Karschin zauważył rektor szkoły we Wschowie – Ribov .
Kobieta zaczęła pisać więcej okolicznościowych wierszy .
Zyskała rozgłos i wsparcie m.in .
