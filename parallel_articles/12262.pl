Vacoas-Phoenix
Vacoas-Phoenix – miasto na Mauritiusie , w środkowej części wyspy , w dystrykcie Plaines Wilhems , na północny zachód od Curepipe .
Około 107 tys .
mieszkańców ( 2010 ) .
Przemysł odzieżowy , włókienniczy , gumowy , obuwniczy ; w okolicy plantacje herbaty .
Trzecie co do wielkości miasto kraju .
W mieście rozwinął się przemysł odzieżowy , włókienniczy , gumowy oraz obuwniczy .
