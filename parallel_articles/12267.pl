Val-en-Vignes
Val-en-Vignes – gmina we Francji , w regionie Nowa Akwitania , w departamencie Deux-Sèvres .
W 2013 roku populacja ludności wynosiła 2022 mieszkańców .
Gmina została utworzona 1 stycznia 2017 roku z połączenia trzech ówczesnych gmin : Bouillé-Saint-Paul , Cersay oraz Massais .
Siedzibą gminy została miejscowość Cersay .
