Anna Maria Jopek
Anna Maria Jopek ( ur .
14 grudnia 1970 w Warszawie ) – polska piosenkarka , kompozytorka , autorka tekstów , producentka muzyczna , reprezentantka Polski w 42 .
Konkursie Piosenki Eurowizji .
Zdobyła dziewięć złotych i siedem platynowych płyt .
Życiorys .
Kariera muzyczna .
Jest absolwentką Akademii Muzycznej w Warszawie w klasie fortepianu .
Studiowała też na wydziale jazzu w Manhattan School of Music w Nowym Jorku .
