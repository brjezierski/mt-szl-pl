Valdensinia heterodoxa
Valdensinia heterodoxa Peyronel – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Grzyb mikroskopijny pasożytujący na liściach wielu gatunków roślin .
U borówek wywołuje chorobę zwaną plamistością liści borówki .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Sclerotinia , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Naukowa nazwa gatunkowa pochodzi od Doliny Waldensów w północno-zachodnich Włoszech .
Znaleziono w niej borówkę czarną porażoną przez tego patogena .
Należy do monotypowego taksonu `` Valdensinia '' Peyronel 1953 .
Morfologia i rozwój .
Patogen zimuje na porażonych , opadłych liściach roślin .
Wzdłuż ich nerwów wytwarza sklerocja , w których na trzonkach o długości 2–6 mm powstają owocniki typu apotecjum .
Początkowo są one wklęsłe , z czasem jednak stają się wypukłe .
Rozwijają się w nich cylindryczne , rozszerzające się ku podstawie , cienkościenne worki bez wieczek .
W każdym z nich powstaje po 8 przezroczystych i bezbarwnych , zwykle elipsoidalnych askospor o wymiarach 10–12 × 4,5 µm .
Apotecja powstają jednak bardzo rzadko .
Anamorfa wytwarza pojedyncze , krótkie , cylindryczne , przezroczyste i bezbarwne konidiofory o szerokości 35–45 µm .
W znajdujących się na ich szczycie komórkach konidiotwórczych powstają wielokomórkowe , gwiaździstego kształtu makrokonidia o średnicy 450–600 µm ( tzw .
staurokonidia ) .
Posiadają 4–5 promienistych , szydlasto zakończonych ramion .
Kulista , centralna część makrokonidiów ma średnicę 100–150 µm .
Na górnej jej powierzchni powstaje około 40–60 cienkościennych , kulistych guzków .
Z czasem powstają z nich maczugowate wypukłości , oddzielone od centralnej części ścianami .
Na górnej stronie ramion powstaje 8–12 cienkościennych , ułożonych poprzecznie i ściśle do siebie przylegających guzków .
Guzki nad centralną częścią i na grzbiecie ramion makrokonidiów razem tworzą palisadową , ściśniętą strukturę .
Przy dużej wilgotności komórki tej palisady pęcznieją , co powoduje wyginanie się końców ramion w dół .
Anamorfa wytwarza również jednokomórkowe i kuliste mikrokonidia o średnicy 2–2,5 µm .
Powstają one na krótkich lub długich , bezbarwnych lub słabo wybarwionych i wzniesionych konidioforach .
Mikrokonidia obserwowano jednak tylko w grzybni hodowanej na sztucznych podłożach .
Występowanie i siedlisko .
Występuje w Europie , Ameryce Północnej oraz Azji .
W Polsce notowany był już w latach 20 .
XX wieku .
`` Valdensinia heterodoxa '' jest polifagiem zasiedlającym wiele gatunków roślin zielnych , krzewinek , krzewów i drzew .
W Europie występuje na roślinach należących do gatunków i rodzajów `` Asplenium filix-femina '' , `` Betula pubescens '' , `` Convallaria majalis '' , `` Corylus avellana '' , `` Fagus sylvatica '' , `` Hieracium '' , `` Hypericum '' , `` Oxalis acetosella '' , `` Polygonatum multiflorum '' , `` Quercus petraea '' , `` Quercus robur '' , `` Rubus '' , `` Scorzonera humilis '' , `` Sorbus aucuparia '' , `` Trientalis europaea '' , `` Vaccinium myrtillus '' , `` Vaccinium vitis-idaea '' .
Zastosowanie .
`` Valdensinia heterodoxa '' jest wykorzystywana w Kanadzie jako bioherbicyd do zwalczania rośliny `` Gaultheria shallon '' , która w lasach iglastych nadmiernie się rozprzestrzeniła i zagłusza siewki drzew .
Usuwanie jej mechaniczne jest trudne i kosztowne , chemiczne mało skuteczne .
