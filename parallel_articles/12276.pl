Walencja ( językoznawstwo )
Walencja – pojęcie z zakresu językoznawstwa określające łączliwość elementów języka , a zwłaszcza czasowników .
Historia .
Pierwsze prace o zjawisku walencyjności przedstawił Karl Bühler , chociaż jego analiza bywa utożsamiana z konotacją .
W następnych latach badania i rozwój teorii walencji dotyczył głównie gramatyki niemieckiej .
Za autora terminu „ walencja ” uznawany jest , który porównał łączenie leksemów w zdania do łączenia atomów w cząsteczki i odwołał się do chemicznej wartościowości .
Teoria walencji miała być alternatywną teorią składniową , która sprawdzała się w opisywaniu czasowników .
Znalazła ona zastosowanie w przetwarzaniu języków naturalnych , jak również jako ogólne źródło w nauczaniu języka obcego .
Teoria .
Teoria walencji dzieli zbiór podrzędników na dwie klasy .
Elementy ściśle związane z nadrzędnikiem określane są mianem `` argumentów '' , `` fraz wymaganych '' lub `` aktantów '' .
Pozostałe , które są luźno związane , nazywa się `` modyfikatorami '' , `` frazami luźnymi '' , `` adiunktami '' lub `` cyrkumstantami '' .
Stałym problemem w teorii walencji jest wyodrębnianie elementów charakterystycznych od elementów adiunktywnych .
Z powodu trudności w określeniu granicy między `` argumentami '' a `` modyfikatorami '' rozważa się istnienie słowników i gramatyki bez takiego rozróżnienia .
