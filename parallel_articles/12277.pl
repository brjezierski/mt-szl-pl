Komunikacja .
Miasto dysponuje dobrym połączeniem z całą Francją , ale też z Belgią .
W głąb Francji można dojechać pociągami linii SNCF , które obsługiwane są taborem TGV .
Miasto posiada połączenie autostradą z Paryżem oraz z Brukselą .
Miasto .
W centrum Valenciennes znajduje rynek z otaczającymi go kamienicami oraz Ratuszem ( fr .
`` l'Hôtel de ville '' ) pochodzącym z XVIII wieku .
Przed ratuszem znajdują się fontanny .
Co około 15 sekund zmieniają sposób wystrzelenia wody .
Na środku rynku usytuowana jest iglica mająca 45 metrów wysokości , wybudowana w 2007 roku .
Na rynku znajduje się także galeria handlowa oraz wiele kawiarenek .
