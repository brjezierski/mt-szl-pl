Valentia
Valentia ( irl .
: `` Dairbhre '' ) – wyspa na Oceanie Atlantyckim , leżąca na zachód od półwyspu Iveragh , z którym połączona jest mostem im .
Maurice ' a O'Neilla .
Administracyjnie należy do irlandzkiego regionu Munster , a dokładniej do hrabstwa Kerry .
Wyspa ma 11 km długości i średnio 3 km szerokości .
Jej najwyższy szczyt , Fogher , osiąga wysokość 268 m n.p.m .
Główną miejscowością wyspy jest Knightstown .
Stacja telegraficzna .
Wyspa znana jest jako wschodni punkt końcowy transatlantyckiego kabla telegraficznego , łączącego ją ze wschodnim wybrzeżem Nowej Fundlandii .
Pierwszy udany przesył wiadomości z wyspy tą drogą miał miejsce w 1857 roku .
Stałe połączenie uruchomione zostało w 1866 i było użytkowane do 1966 , kiedy to firma Western Union International zrezygnowała z jego eksploatacji .
Obecnie w miejscu dawnej stacji telegraficznej , nazwanym Telegraph Field , znajduje się pamiątkowy monument .
Kamieniołomy .
Na wyspie eksploatowane kamieniołomy łupków .
Tutejszy surowiec wykorzystano m.in .
przy budowie gmachu brytyjskiego Parlamentu oraz Opery Paryskiej .
Odkryto tu jedne z najstarszych znanych skamieniałych tropów wczesnych tetrapodów – ich wiek ocenia się na podstawie badań radiometrycznych na około 385 mln lat .
Infrastruktura i komunikacja .
Na wyspie znajduje się port morski oraz latarnia morska .
W północnej części wyspy położony jest ogród z roślinami tropikalnymi , Glanleam House .
Na Valentię można się dostać drogą lądową przez most z Portmagee , lub promem z Reenard Point .
