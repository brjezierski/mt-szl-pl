Anna Myszyńska
Anna Myszyńska z domu Schneider ( ur .
30 czerwca 1931 w Kórnicy , zm .
7 września 2019 w Nysie ) – polska pisarka , fotograf , poetka , propagatorka śląskiej gwary i tradycji .
Życiorys .
Pochodziła z rodziny śląsko-niemieckiej .
Szkołę podstawową ukończyła w języku niemieckim w 1945 .
Po zakończeniu II wojny światowej przez parę lat pracowała na gospodarstwie swoich rodziców i na żniwach w Lipnie w Prudniku .
Nie uczęszczała do szkoły , ponieważ nie znała języka polskiego , tylko gwarę śląską .
Dopiero w 1952 poszła do szkoły pielęgniarskiej w Raciborzu , którą ukończyła jako autodydaktyk z wyróżnieniem i otrzymała pracę w szpitalu w Opolu .
W 1957 ukończyła Państwową Szkołę Położnych w Nysie .
Zamieszkała w Białej Prudnickiej .
Zajęła się fotografią dzięki swojemu mężowi Bogdanowi Myszyńskiemu , z którym prowadziła zakład fotograficzny .
Po przejściu na emeryturę została fotoreporterem gazety „ Panorama Bialska ” .
Wzięła udział w konkursie gwary śląskiej „ Po naszemu , czyli po Śląsku ” , organizowanym przez Polskie Radio Katowice .
Zaczęła pisać do gazety krótkie opowiadania po śląsku „ Jak to piyrwej było ” , w których wspominała swoje dzieciństwo , młodość , święta kościelne , stare zwyczaje i obrzędy związane z porami roku i z pracą na wsi , nawiązując do obecnych czasów .
Ze względu na brak odrębnego alfabetu dla zapisu śląskiej mowy , tekst jako autodydaktyk zapisywała tak , jak sama mówiła i słyszała .
W 1994 otrzymała od Radia Opole propozycję współpracy w prowadzonej przez redaktora Andrzeja Russaka dla mniejszości niemieckiej audycji „ Nasz Heimat ” .
Współpracowała też z „ Tygodnikiem Prudnickim ” , dla którego przetłumaczyła na język polski treść powieści `` Lato umarłych snów '' Harry ’ ego Thürka .
Była pomysłodawcą Konkursu Gwary Śląskiej w Prudniku .
Zmarła 7 września 2019 w hospicjum w Nysie .
