Valletta
Valletta ( ) – jednostka administracyjna i stolica Malty położona na półwyspie Sciberras pomiędzy dwiema zatokami : Grand Harbour i Marsamxett .
Jest najdalej wysuniętą na południe europejską stolicą .
Valletta z 320 zabytkami , jest jednym z najbardziej zagęszczonych obszarów zabytkowych na świecie .
Znajduje się na liście światowego dziedzictwa UNESCO .
Niekiedy nazywana „ muzeum pod gołym niebem ” .
Została wybrana Europejską Stolicą Kultury 2018 .
Valletta jest najbardziej słoneczną miejscowością w Europie .
