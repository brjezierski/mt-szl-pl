Anna-Maria Rawnopolska-Dean
Anna-Maria Rawnopolska-Dean ( bułg .
Анна-Мария Равнополска-Дийн ; ur .
3 sierpnia 1960 w Sofii ) – bułgarska kompozytorka , harfistka i pedagog .
Początkowo studiowała u włoskiej pedagog i harfistki Liany Pasquali , a następnie kontynuowała kształcenie w USA u Susann McDonald na Indiana University w Bloomington .
W 1992 r. zadebiutowała w Carnegie Hall .
