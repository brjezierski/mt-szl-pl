Valmiera ( skocznia narciarska )
Valmiera – największy na Łotwie obiekt umożliwiający oddawanie skoków narciarskich ; znajduje się w miejscowości Sigulda .
Punkt konstrukcyjny skoczni umieszczony jest na 50 metrze .
Skocznia powstała w 1933 , rozgrywano na niej mistrzostwa kraju .
Ostatnie zawody na Valmierze odbyły się w 1978 roku .
W 2007 opublikowano plany budowy w tym miejscu kompleksu skoczni narciarskich , z których największa miała mieć punkt konstrukcyjny umieszczony na 70. metrze .
W 2009 oddano do użytku trzy małe obiekty ( K18 , K10 i K5 ) .
