Valsaria insitiva
Valsaria insitiva ( Tode ) Ces .
& amp ; De Not .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny powodujący choroby roślin .
W Polsce wywołuje cytosporozę jabłoni i leukostomozę drzew pestkowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Valsaria , Valsariaceae , Valsariales , Incertae sedis , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1791 r. Heinrich Julius Tode nadając mu nazwę `` Sphaeria insitiva '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Vincenzo de Cesati i Giuseppe De Notaris w 1863 r .
Ma ponad 80 synonimów .
Jednym z nich jest anamorfa `` Leucostoma cinctum '' , dawniej opisywana jako odrębny gatunek .
Morfologia i rozwój .
Saprotrof żyjący na korze drzew , a w sprzyjających warunkach zamieniający się w jej pasożyta .
Atakuje głównie drzewa uszkodzone przez mróz lub osłabione niedoborem wody .
Na podkładkach tworzy konidiomy , często wielokomorowe , kręte i promieniście odchodzące od środka .
Konidiofory szkliste , septowane , nieregularnie rozgałęzione u podstawy i powyżej .
Komórki konidiotwórcze nie różniące się wyraźnie morfologicznie od konidioforów , zwykle tworzą długie , wyraźne gałęzie , proste , szkliste , gładkie , proliferujące jednocześnie .
