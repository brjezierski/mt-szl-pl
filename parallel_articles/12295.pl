Vangelis
Vangelis ( gr .
Βαγγέλης ) , właśc .
Ewangelos Odiseas Papathanasiu ( gr .
Ευάγγελος Οδυσσέας Παπαθανασίου ; ur .
29 marca 1943 w Agrii koło Wolos , zm .
17 maja 2022 w Paryżu ) – grecki kompozytor muzyki elektronicznej i filmowej .
Charakterystyczną cechą jego muzyki jest bogate brzmienie instrumentów elektronicznych .
Życiorys .
Okres młodzieńczy .
Vangelis urodził się 29 marca 1943 roku w Agrii koło Wolos , w Grecji .
Edukację muzyczną rozpoczął w wieku 4 lat , a już w wieku 6 lat miał swój pierwszy publiczny występ , na którym prezentował własne kompozycje .
Vangelis był w dużej części samoukiem – odmawiał pobierania lekcji gry na pianinie , a w ciągu swojej kariery nie zdobył formalnej wiedzy na temat notacji muzycznej .
Uczył się muzyki poważnej , malarstwa i reżyserii filmowej na Akademii Sztuk Pięknych w Atenach .
We wczesnych latach sześćdziesiątych założył grupę `` Forminx '' , która stała się w Grecji bardzo popularna .
W 1968 roku pod wpływem zamieszek studenckich w Grecji przeniósł się do Paryża , gdzie założył wraz z Demisem Roussosem i Loukasem Siderasem zespół `` Aphrodite ’ s Child '' ( `` Dziecko Afrodyty '' ) .
Grupa została rozwiązana w 1972 roku .
Początek kariery solowej .
Twórczość solową Vangelis rozpoczął pisząc muzykę do dwóch filmów Frederica Rossifa w 1973 roku .
Pierwszym oficjalnym solowym albumem Vangelisa był `` Earth '' z 1974 roku .
W tym okresie Vangelis odbywał też próby z zespołem Yes , do którego jednak nie dołączył .
Zaprzyjaźnił się jednak z Jonem Andersonem , wokalistą zespołu , z którym później wielokrotnie współpracował .
Po przeprowadzce do Londynu Vangelis podpisał kontrakt z RCA Records , założył własne studio ( Nemo Studios ) i rozpoczął nagrywanie własnych albumów elektronicznych .
Razem z Jonem Andersonem wydał w latach 80. i 90. kilka albumów jako Jon & amp ; Vangelis .
Muzyka filmowa i sukces komercyjny .
W 1982 roku Vangelis dostał Oscara za muzykę do filmu `` Rydwany ognia '' .
W tym samym roku rozpoczął współpracę z Ridleyem Scottem , co zaowocowało stworzeniem muzyki do filmów `` Łowca androidów '' i `` 1492 .
Wyprawa do raju '' .
Vangelis jest również autorem muzyki do wielu filmów dokumentalnych Jacques ’ a Cousteau .
Zasiadał w jury konkursu głównego na 44 .
MFF w Cannes ( 1991 ) .
W 1992 Francja uhonorowała go Orderem Sztuki i Literatury .
Wydana w 2001 roku Mythodea ( pierwotnie napisana w roku 1993 ) została użyta przez NASA jako motyw marsjańskich misji kosmicznych .
W roku 2004 został autorem ścieżki dźwiękowej do filmu „ Aleksander ” .
Jest także twórcą hymnu piłkarskich mistrzostw świata , które odbyły się w 2002 roku w Korei i Japonii .
