Vantaa
Vantaa ( szw .
`` Vanda '' ) – miasto i gmina w Finlandii , w regionie Uusimaa nad rzeką Vantaanjoki .
Ma 204 193 mieszkańców ( 30.05.2012 ) , co daje mu czwartą co do liczby ludności pozycję w kraju .
Wraz z Espoo , Kauniainen i Helsinkami tworzy główny trzon aglomeracji helsińskiej ( tak zwany Region Stołeczny , fiń .
`` Pääkaupunkiseutu '' ) .
W mieście znajduje się główny port lotniczy kraju – Helsinki-Vantaa .
Vantaa zajmuje powierzchnię 240,34 km² , z czego 1,97 km² stanowi woda .
Do 1972 roku funkcjonowała pod nazwą `` Helsingin maalaiskunta '' ( gmina ziemska Helsinki ) , w 1972 uzyskała status miasta targowego ( `` kauppala '' ) , a w 1974 prawa miejskie ( `` kaupunki '' ) .
Miasto graniczy z gminami : Helsinki , Espoo , Nurmijärvi , Kerava , Tuusula i Sipoo .
