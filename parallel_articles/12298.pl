Vanuatu
Republika Vanuatu ( bislama : `` Ripablik blong Vanuatu '' ) – państwo w Oceanii .
Położone na 198 wyspach Nowych Hebrydów ( 2 z nich – Matthew i Hunter – należą do Nowej Kaledonii , terytorium zamorskiego Francji ) , z których 65 jest zamieszkanych .
Położone jest ok. 800 km na północny zachód od Fidżi i na północny wschód od Nowej Kaledonii .
Dawniej terytorium kraju było kondominium francusko-brytyjskim znanym pod nazwą Nowe Hebrydy .
Geografia .
Vanuatu obejmuje 198 ( w tym : 65 zamieszkanych ) przeważnie górzystych wysp pochodzenia wulkanicznego , m.in .
Espiritu Santo , Malekula , Efate , Ambrim , Erromango .
Występują wąskie pasy nizin nadbrzeżnych , na trzech wyspach znajdują się czynne wulkany .
Odnotowuje się częste trzęsienia ziemi , powodujące tsunami .
Największe miasto Port Vila – stolica , na wyspie Efate – 45 694 mieszkańców ( X 2009 ) , drugie większe miasto – Luganville ( 13 484 mieszkańców ) – na Espiritu Santo .
Klimat równikowy wilgotny , tutejsze wiecznie zielone lasy równikowe tworzą swoisty mikroklimat wysp .
