Vararia ochroleuca
Vararia ochroleuca ( Bourdot & amp ; Galzin ) Donk – gatunek grzybów należący do rodziny powłocznicowatych ( `` Peniophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Vararia , Peniophoraceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1911 r. Hubert Bourdot i Amédée Galzin nadając mu nazwę `` Asterostremella ochroleuca '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu w 1930 r. Marinus Anton Donk .
Morfologia .
Jednoroczny , rozpostarty , tworzący rozproszone łaty o nieregularnym kształcie , czasami rozrzedzone o kolistym lub wydłużonym kształcie .
Pojedynczy owocnik ma długość do 20 cm , sąsiednie owocniki często zrastają się z sobą .
