Varese
( , pol .
uproszczona : `` wareze '' ) – miasto i gmina w północnych Włoszech , w regionie Lombardia , siedziba prowincji Varese .
Varese leży nad jeziorem Varese , na przedgórzu Alp .
Według danych na rok 2004 gminę zamieszkiwały 82 282 osoby , 1523,7 os./km² .
Wielki ośrodek przemysłu skórzanego i obuwniczego oraz środków transportu , elektroniczny , włókienniczy , chemiczny , spożywczy .
