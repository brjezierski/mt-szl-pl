Varnsdorf ( stacja kolejowa )
Varnsdorf – stacja kolejowa w Varnsdorfie , w kraju usteckim , w Czechach .
Jest ważnym węzłem kolejowym o znaczeniu regionalnym .
Znajduje się na wysokości 340 m n.p.m .
Jest zarządzana przez Správę železnic .
Na stacji nie ma możliwości zakupu biletu , a obsługa podróżnych odbywa się w pociągu .
