Vaucresson
Vaucresson – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Według danych na rok 2012 gminę zamieszkiwało 8 611 osób , a gęstość zaludnienia wynosiła 2 796 osób/km² .
Wśród 1287 gmin regionu Île-de-France Vaucresson plasuje się na 810. miejscu pod względem powierzchni .
