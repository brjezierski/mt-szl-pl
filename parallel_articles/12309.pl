Vaxholm
Vaxholm − miasto i twierdza morska w Szwecji , w regionie Sztokholm ( szw .
`` Stockholms län '' ) , siedziba gminy Vaxholm , liczące około 4 817 mieszkańców .
Nazwa miasta wywodzi się od Zamku Vaxholm wybudowanego w 1549 roku , w pobliżu którego Gustaw I Waza założył w 1558 roku miasto .
Prawa miejskie uzyskało ono w 1652 roku .
