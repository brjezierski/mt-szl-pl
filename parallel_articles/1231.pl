Annapolis ( film 2006 )
Annapolis – amerykański film z 2006 w reżyserii Justin Lin .
W rolach głównych występują James Franco , Tyrese Gibson , Jordana Brewster , Donnie Wahlberg , Roger Fan , Chi McBride i Vicellous Reon Shannon .
Film kręcono w Filadelfii i na uniwersytecie w Princeton .
W filmie jest ukazana historia rekruta `` Amerykańskiej Akademii Marynarki Wojennej '' , który pragnie spróbować własnych sił w środowisku wrogim .
Film jest opowieścią o sile , honorze i uporze , z którym możne realizować swoje marzenia .
