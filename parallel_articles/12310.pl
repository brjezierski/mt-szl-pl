Vaya Con Dios ( z jęz .
hiszp .
`` Idź z Bogiem '' ) – belgijski zespół muzyczny założony w 1986 roku przez Dani Klein , Dirka Schoufsa , i Willy'ego Lambregta , który później został zastąpiony przez Jean-Michel Gielena .
Największe przeboje zespołu to : „ Don ’ t Cry for Louie ” , „ Nah Neh Nah ” , „ Puerto Rico ” , „ What ’ s a Woman ” , „ Time Flies ” , „ Heading for a Fall ” oraz „ Don ’ t Break My Heart ” .
Zespół działał do 1996 roku .
W 2004 roku , po ośmiu latach przerwy zespół reaktywował się .
25 października 2014 roku w Brukseli odbył się pożegnalny koncert zespołu .
Materiał rejestrowany podczas koncertu został wydany na płycie zatytułowanej `` Thank You All '' , która ukazała się 8 grudnia 2014 roku .
