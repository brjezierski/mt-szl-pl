Vedran Ćorluka
Vedran Ćorluka ( wym .
[ ] ; ur .
5 lutego 1986 w Dervencie ) – chorwacki piłkarz , grający na pozycji obrońcy .
Kariera klubowa .
Urodził się w Bośni i Hercegowinie .
Karierę piłkarską zaczynał w chorwackim Dinamo Zagrzeb ; w młodym wieku trafił do tamtejszej szkółki piłkarskiej .
Jako nastolatek nie miał szans na grę w pierwszym składzie i został wypożyczony do innego pierwszoligowca Inter Zaprešić , w którego barwach zadebiutował 24 lipca 2004 w lidze , w wygranym 1:0 meczu z NK Zagreb .
Z czasem trafił do pierwszego składu i do końca sezonu był czołowym zawodnikiem defensywy Interu .
Klub zajął bardzo dobre drugie miejsce w lidze i osiągnął jeden z największych sukcesów w historii .
Po świetnym sezonie Ćorluka powrócił do stołecznego Dinama .
W sezonie 2005/2006 zagrał we wszystkich 32 meczach ligowych , wraz z klubem zdobył mistrzostwo kraju .
