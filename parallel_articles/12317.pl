Veldhoven
Veldhoven – miasto i gmina w prowincji Brabancja Północna w Holandii .
Liczy sobie 45 337 mieszkańców ( 2019 )
W małej miejscowości Halfmijl znajdują się neolityczne kurhany .
W południowej części gminy płyną krótkie rzeki Gender i Run .
Granicę między Veldhoven a Waalre stanowi rzeka Dommel .
W gminie znajduje się Muzeum 't Oude Slot , poświęcone etnologii .
Jest tam też specjalne zoo , które koncentruje się na ochronie papug i innych ptaków egzotycznych. . W gminie znajduje się także kilka kościołów oraz trzy wiatraki .
Miastem partnerskim jest Jarocin .
