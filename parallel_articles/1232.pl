Annecy ( jezioro )
Annecy ( fr .
`` Lac d ’ Annecy '' ) – jezioro lodowcowe we Francji ( Górna Sabaudia ) , w dorzeczu rzeki Rodan , w Alpach Sabaudzkich .
Annecy to drugie co do wielkości jezioro we Francji , z wyjątkiem francuskiej części Jeziora Genewskiego .
Dzięki ścisłym ograniczeniom dotyczącym ochrony środowiska , wprowadzonym w latach 90. , jezioro to znane jest jako jedno z najczystszych w Europie .
Wśród turystów słynie z możliwości uprawiania wielu sportów wodnych oraz położonych w pobliżu pozostałości prehistorycznych budowli palowych .
Jest także pierwszym dużym jeziorem alpejskim , które już w latach 80. zostało całkowicie otoczone siecią dróg rowerowych .
Jezioro powstało około 18 000 lat temu , podczas przesuwania się lodowca alpejskiego .
Jest zasilane przez wiele pomniejszych rzek spływających z okalających je gór ( Ire , Eau morte , Laudon , Bornette i Biolon ) oraz potężne podwodne źródło , Boubioz , zlokalizowane na głębokości 80 m .
