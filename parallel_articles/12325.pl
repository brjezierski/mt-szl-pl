`` Windau '' ) – rzeka w północnej Litwie i w zachodniej Łotwie ( Kurlandia ) .
Długość – 346 km , z tego 178 na Łotwie , powierzchnia zlewni – 11 800 km² , z czego 67 % na Łotwie , 33 % na Litwie .
Windawa ma źródła w okolicach miasta Kurszany ( `` Kuršėnai '' ) , w okręgu szawelskim ( `` Šiaulių apskritis '' ) Litwy .
Do Morza Bałtyckiego uchodzi w łotewskim porcie Windawa .
Główne miasta położone nad Windawą to Możejki , Kuldīga i Windawa .
Najdłuższe dopływy to : Abava , Wyrwita i Varduva .
