Morfologia i rozwój .
Pseudotecja rozwijają się w tkance liścia .
W stanie dojrzałym są kuliste lub prawie kuliste , z ostiolą , brązowe , o średnicy 60–160 µm .
Worki workowate , cylindryczne , bitunikowe , 8-zarodnikowe , o wymiarach 45–60 × 5-6 µm .
Askospory oliwkowe , dwukomórkowe , górna komórka jest szersza niż dolna , zwężająca się komórka .
Mają wymiar 12–16 × 3–5 µm .
Anamorfa w hodowli na sztucznym podłożu w postaci watowatej kolonii lub punkcikowato , ciemnooliwkowobrązowa , aksamitna .
Grzybnia rozwija się pod skórką lub pod naskórkiem żywiciela .
