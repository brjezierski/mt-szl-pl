Venturia pyrina
Venturia pyrina Aderh.– gatunek grzybów z rodziny `` Venturiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Venturiaceae '' , `` Venturiales '' , `` Pleosporomycetidae '' , `` Dothideomycetes '' , `` Pezizomycotina '' , `` Ascomycota '' , `` Fungi '' .
Niektóre synonimy :
Dawniej bezpłciowo rozmnażające się stadia opisano jako odrębny gatunek – `` Fusicladium pyrorum '' ( Lib . )
Fuckel 1870 , stąd też w literaturze spotkać się możemy z opisem , że parcha gruszy wywołuje `` Venturia pyrina '' oraz konidialne stadium `` Fusicladium pyrorum '' .
Od czasu jednak , kiedy udowodniono , że `` Fusicladium pyrorum '' i `` Venturia pyrina '' to ten sam gatunek , zgodnie z zasadami nomenklatury mykologicznej nosi on nazwę stadium rozmnażającego się płciowo , czyli `` V. pyrina '' , zaś `` F. pyrorum '' to tylko jego synonim .
Cykl rozwojowy .
`` Venturia pyrina '' zimuje w postaci owocników na opadłych z drzewa i porażonych jeszcze w okresie sezonu wegetacyjnego liściach , oraz w postaci grzybni na pąkach i pędach porażonych drzew gruszy .
Zimą i wiosną na opadłych liściach powstają owocniki typu pseudotecjum , a w nich w sposób płciowy askospory .
Dokonują one infekcji pierwotnej .
