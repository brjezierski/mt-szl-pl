Venturia saliciperda
Venturia saliciperda Nüesch – gatunek workowców z rodziny `` Venturiaceae '' .
Wywołuje chorobę wierzb zwaną parchem wikliny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Venturiaceae '' , `` Venturiales '' , `` Pleosporomycetidae '' , `` Dothideomycetes '' , `` Pezizomycotina '' , `` Ascomycota '' , `` Fungi '' .
Synonimy :
Morfologia .
Pseudotecja okrągłe , zanurzone w tkankach żywiciela .
Mają średnicę 80-120 μm i brodawkowatą ostiolę .
W naturze pojawiają się bardzo rzadko .
Worek cylindryczny , 8-zarodnikowy , bitunikowy , o rozmiarach 35-55 x 8-12 μm .
Askospory wąskoeliptyczne , żółtawozielone , podzielone przegrodą zlokalizowaną w 1/3 wysokości zarodnika .
