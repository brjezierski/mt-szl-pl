Veranópolis
Veranópolis – miasto w południowej Brazylii , w stanie Rio Grande do Sul , leżące w regionie Serra Gaúcha .
W 2006 roku przybliżona liczba mieszkańców wynosiła 21 351 .
Zajmuje powierzchnię 289,432 km² .
Duże skupisko Polonii .
Historia .
Veranópolis zostało założone przez włoskich i polskich emigrantów , a początki kolonizacji sięgają 1984 roku .
Miasto otrzymało prawa miejskie 15 stycznia 1884 .
