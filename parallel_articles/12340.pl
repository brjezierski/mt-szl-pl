Wersal
Wersal ( fr .
`` Versailles '' ) – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Yvelines .
Budowę Wersalu rozpoczęto w roku 1668 .
Znajduje się pod Paryżem i słynie z zespołu pałacowego , miejsca rezydencji królów francuskich od 1682 roku .
Pałac wersalski , rozbudowany z rozmachem za Ludwika XIV .
Twórcą założenia ogrodowo-parkowego był André Le Nôtre .
W Wersalu w 1789 r. zwołane zostały Stany Generalne , w 1871 pałac był siedzibą rządu ( wersalczycy ) , który stłumił Komunę Paryską .
Miejsce wielu konferencji międzynarodowych , między innymi został tu podpisany Traktat wersalski 28 czerwca 1919 .
