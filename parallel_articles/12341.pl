Versoix ( stacja kolejowa )
Versoix ( fra : `` Gare de Versoix '' ) – stacja kolejowa w Versoix , w kantonie Genewa , w Szwajcarii .
Znajduje się na linii kolejowej Lozanna – Genewa .
