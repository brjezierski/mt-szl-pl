Verticillium albo-atrum
Verticillium albo-atrum Reinke & amp ; Berthold – gatunek grzybów należący do klasy `` Sordariomycetes '' .
W Polsce jest organizmem kwarantannowym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Plectosphaerellaceae , Glomerellales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Hodowany w temperaturze 23° C na agarze z dekstrozą ziemniaczaną i na agarze słodowym rośnie szybko i tworzy kłączkowatą grzybnię , początkowo o białej , potem śmietankowej barwie .
Po 2-3 tygodniach zmienia barwę na brązowo-czarną w wyniku wytwarzania w starszych koloniach grubościennej grzybni przetrwalnikowej .
Konidiofory liczne , mniej lub bardziej wyprostowane , bezbarwne , spiralnie rozgałęzione .
Na każdej gałązce końcowej znajdują się 2-4 fialidy , które wtórnie rozgałęziają się .
Charakterystyczne jest zaciemnienie podstawy fialid , gdy patogen rozwija się na tkankach roślinnych .
