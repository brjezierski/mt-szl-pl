Verticillium dahliae
Verticillium dahliae Kleb.– gatunek grzybów należący do klasy `` Sordariomycetes '' .
W Polsce ma status organizmu kwarantannowego .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Verticillium , Plectosphaerellaceae , Glomerellales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Występuje w postaci anamorfy , teleomorfa nie jest znana .
Morfologia .
Hodowany w temperaturze 23 °C na agarze z dekstrozą ziemniaczaną i na agarze słodowym tworzy grzybnię o białej lub śmietankowej barwie .
Po pewnym czasie zmienia barwę na czarną w wyniku wytwarzania w starszych koloniach dużej ilości czarnych mikrosklerocjów .
Konidiofory mniej lub bardziej wyprostowane , bezbarwne , rozgałęzione .
Na każdej gałązce końcowej znajdują się 3-4 fialidy , niekiedy wtórnie dzielące się .
Fialidy są zróżnicowanej wielkości , najczęściej mają długość 16–35 μm i grubość 1–2,5 μm .
Konidia powstają pojedynczo na wierzchołkach fialid .
Mają kształt od elipsoidalnego do półcylindrycznego , są bezbarwne , nieseptowane lub z jedną przegrodą .
