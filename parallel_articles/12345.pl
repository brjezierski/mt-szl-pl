Verticillium tricorpus
Verticillium tricorpus I. Isaac – gatunek grzybów należący do klasy `` Sordariomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Verticillium , Plectosphaerellaceae , Glomerellales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Morfologia .
Hodowany w temperaturze 23° C na agarze z dekstrozą ziemniaczaną i na agarze słodowym rozwija się szybko .
Tworzy grzybnię , początkowo o głęboko złotożółtej barwie , potem pomarańczowo-żółtej .
Po 2-3 tygodniach zmienia ona barwę na czarniawą w wyniku wytwarzania w starszych koloniach dużej ilości czarnych mikrosklerocjów .
Grzybnia jest wełnista , niezbyt zbita , złożona z pomarańczowo-żółtych strzępek o grubości 2–3 μm z gutulami .
Po około 7 dniach hodowli pojawiają się białe konidiofory , w ciągu następnych 2-3 tygodni nadające grzybni białawą barwę .
Konidiofory występują obficie , są mniej lub bardziej wyprostowane i tworzą spiralne rozgałęzienia .
Przy każdym rozgałęzieniu tworzą się 3-4 fialidy .
