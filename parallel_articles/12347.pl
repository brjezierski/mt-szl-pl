Verviers ( 1956 )
Verviers ( M934 ) – belgijski trałowiec z lat 50 .
XX wieku , jedna z 26 zbudowanych dla Belgijskiej Marynarki Wojennej jednostek amerykańskiego typu `` Adjutant '' .
Stępkę okrętu położono w październiku 1954 roku w stoczni Hodgdon Brothers w East Boothbay , a w skład marynarki wojennej Belgii został przyjęty 4 czerwca 1956 roku .
W 1972 roku okręt przebudowano na niszczyciel min .
Jednostkę wycofano ze służby w 1987 roku .
Projekt i budowa .
Projekt trałowców typu `` Adjutant '' ( `` Blackbird '' ) powstał jako rozwinięcie drugowojennych jednostek typu `` YMS '' .
Okręty miały w większości zasilić floty państw sojuszniczych , w ramach Major Defense Acquisition Program ( MDAP ) .
