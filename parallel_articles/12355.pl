Vicenza
( , pol .
uproszczona : `` wiczenca '' ) – miasto i gmina w północno-wschodnich Włoszech , w regionie Wenecja Euganejska , w prowincji Vicenza .
Leży u podnóży Alp Weneckich , nad rzeką Bacchiglione w dorzeczu Brenty , w odległości ok. 60 km na zachód od Wenecji i ok. 200 km na wschód od Mediolanu .
W grudniu 2008 Vicenza liczyła 115 000 mieszkańców , a jej obszar metropolitalny 270 000 osób .
Vicenzę rozsławił przede wszystkim XVI-wieczny renesansowy architekt Andrea Palladio .
