Vichy
Vichy ( prow .
`` Vichèi '' ) – miejscowość i gmina w środkowej Francji , w regionie Owernia-Rodan-Alpy , w departamencie Allier , na Masywie Centralnym .
Historia .
Czasy Cesarstwa Rzymskiego .
W 52 r. p.n.e .
wracając po przegranej bitwie pod Gregovią Rzymianie założyli na brzegu rzeki Flumen Elaver ( dzisiaj Allier ) osadę .
Jej mieszkańcy używali wody z pobliskich gorących źródeł do celów zdrowotnych .
Przez dwa pierwsze wieki nowej ery Vichy dynamicznie się rozwijało dzięki ich wykorzystywaniu .
