Na temat rodziny Victora Hugo i jego przodków w literaturze biograficznej funkcjonuje wiele mitów i nieścisłości , do których powstania przyczynił się sam poeta – przekazując fałszywe informacje na ten temat żonie , gdy ta opracowywała pierwszy szkic jego biografii .
Powtarzając rodzinną legendę stworzoną przez swego ojca Hugo twierdził , że po mieczu wywodzi się ze starego rodu lotaryńskiego , z której pochodził m.in .
biskup Ptolemaidy Louis Hugo oraz kapitan gwardii książąt lotaryńskich Georges Hugo , w 1535 uszlachcony .
W rzeczywistości , pierwszym znanym bezpośrednim przodkiem Hugo był Claude Hugo , wymieniany w 1631 grabarz z Mirecourt .
Dziadek Hugo od strony ojca , Joseph Hugo , był mistrzem stolarskim z Nancy .
Ojciec pisarza , Joseph Léopold Sigisbert , urodził się w 1773 i zrobił karierę wojskową w armii rewolucyjnej Francji , a następnie był oficerem wojsk napoleońskich .
