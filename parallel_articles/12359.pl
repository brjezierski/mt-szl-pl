Victor Hugo
Victor Marie Hugo ( fr : [ ] ) , Wiktor Hugo ( ur .
26 lutego 1802 w Besançon , zm .
22 maja 1885 w Paryżu ) – francuski pisarz , poeta , dramaturg i polityk .
Jeden z najważniejszych twórców literatury francuskiej i czołowy przedstawiciel romantyzmu francuskiego .
Autor poezji , dramatów wierszem i prozą oraz powieści , jak również listów i przemówień politycznych .
Do jego najważniejszych dzieł należą powieści `` Nędznicy '' i `` Katedra Marii Panny w Paryżu '' , dramat `` Hernani '' oraz jedyna we francuskiej literaturze epopeja `` Legenda wieków '' .
Był członkiem Akademii Francuskiej , deputowanym do Konstytuanty , a następnie Zgromadzenia Narodowego .
Wspierał rewolucję lipcową w 1830 roku , brał udział w rewolucji lutowej w 1848 roku , a z powodu przekonań politycznych przez 19 lat ( 1851–1870 ) przebywał na dobrowolnym wygnaniu poza granicami Francji jako przeciwnik II Cesarstwa .
Po upadku Napoleona III w roku 1870 wrócił do Francji , zasiadał w Senacie III Republiki .
Zmarł na zapalenie płuc i został pochowany w paryskim Panteonie .
Jego utwór `` Notre-Dame de Paris '' umieszczony został w index librorum prohibitorum dekretem z 1834 roku a `` Les misérables '' dekretem z 1864 roku .
