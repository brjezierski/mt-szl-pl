`` Odpicuj mi brykę '' ) .
The High Fructose Adventures of Annoying Orange .
11 czerwca 2012 roku na kanale Cartoon Network zaczęto nadawać program `` The High Fructose Adventures of Annoying Orange '' , stworzony przez studio Gagfilms .
Bohaterowie pozostają niezmienieni , jednak zmieniono otoczenie z kuchni do Supermarketu DaneBoe 's ( odniesienie do twórcy Dana Boedigheimera ) , którego pracownikiem jest Nerville ( grany przez Toby'ego Turnera ) , zaprzyjaźniony z owocami .
