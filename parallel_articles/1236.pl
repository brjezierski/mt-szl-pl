Annoying Orange
Annoying Orange ( pol .
`` Irytująca Pomarańcza '' ) – internetowy serial komediowy autorstwa Dana Boedigheimera udostępniana na YouTube .
Annoying Orange wystartowało 9 października 2009 roku .
Odcinki trwają średnio od półtorej minuty do czterech minut .
Można oglądać również w telewizji ( angielskiej i polskiej MTV ) .
Fabuła .
Główną postacią serialu jest Pomarańcza ( ang .
Orange ) , grana przez Dana Boedigheimera , która wyzywa i uraża wszelkie owoce i warzywa ( w późniejszych odcinkach również inne produkty spożywcze oraz postacie historyczne ) , które znajdują się w jej pobliżu .
Żartuje z nich , a one urażone próbują się odciąć , jednak pomarańcza jest tak irytująca , że nie potrafią i każdy odcinek kończy się podobnie – zirytowany owoc umiera .
Początkowo próbuje ona ostrzec rozmówcę , jednak każdy z nich ( z wyjątkiem Dziadka Cytryny , który po prostu przysnął ) jest zbyt zdenerwowany by słuchać .
Później podobną funkcję pełnią inne nieprzyjemne wydarzenia dotykające innych rozmówców Pomarańczy , takich , jak kopnięcie Piłki Nożnej czy przytrzaśnięcie garncem złota Liama Leprechauna , głównej postaci antagonistycznej .
