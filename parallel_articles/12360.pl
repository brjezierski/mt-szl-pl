Victoria ( Kilar )
Victoria ( pol .
zwycięstwo ) – fanfara na chór mieszany i orkiestrę autorstwa Wojciecha Kilara z 1983 roku , której tekst stanowią słowa zaczerpnięte z listu Jana III Sobieskiego do papieża Innocentego XI ( `` Venimus , vidimus , Deus vicit !
`` ) .
Historia .
Dziełko powstało z okazji Drugiej pielgrzymki Jana Pawła II do Ojczyzny .
Inspiratorem był katowicki bp Herbert Bednorz .
Kantata stała się wyrażeniem oczekiwań związanych z zakończeniem stanu wojennego w Polsce .
Po wysłuchaniu fanfary w katowickiej katedrze papież Jan Paweł II powiedział : `` Bóg zapłać za tę piękną muzykę '' .
W części chóralnej śpiewane są łacińskie słowa : `` Venimus , vidimus , Deus vicit ! ''
( pol .
Przybyliśmy , zobaczyliśmy , Bóg zwyciężył ! )
