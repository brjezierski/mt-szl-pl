Viggo Mortensen
Viggo Peter Mortensen Jr. ( ur .
20 października 1958 w Nowym Jorku ) – amerykański aktor teatralny i filmowy , a także poeta , fotograf , malarz oraz reżyser , scenarzysta i producent filmowy .
Trzykrotnie nominowany do Oscara dla najlepszego aktora pierwszoplanowego .
Życiorys .
Wczesne lata .
Urodził się na nowojorskim Manhattanie jako starszy syn Amerykanki Grace Gamble ( z domu Atkinson ) i Viggo Petera Mortensena Sr. , pochodzenia duńskiego ; jego rodzice spotkali się w Norwegii .
Dziadek ze strony matki pochodził z Nowej Szkocji w Kanadzie , a rodzina babki ze strony matki była z Nowej Anglii .
Jego rodzina przeniosła się do Wenezueli , a następnie do Danii i ostatecznie osiadła w Argentynie w prowincji Córdoba , w Chaco i Buenos Aires , gdzie uczęszczał do szkoły podstawowej i opanował biegle język hiszpański , podczas gdy jego ojciec zajmował się fermą drobiu i ranczem .
