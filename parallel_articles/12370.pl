Villa Carlos Paz
Villa Carlos Paz , również Carlos Paz , niegdyś `` Santa Leocadia '' − miasto położone w Argentynie w prowincji Córdoba , 36 km na zachód od stolicy u podnóża gór Sierras de Córdoba .
Miasto to jest główną bazą Rajdu Argentyny , eliminacji Rajdowych Mistrzostw Świata .
