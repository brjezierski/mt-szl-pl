Villarejo de Salvanés
Villarejo de Salvanés – miasto w Hiszpanii we wspólnocie autonomicznej Madryt , na południowy wschód od stolicy .
Leży pomiędzy dwiema rzekami Tagiem i Tajuñą .
Villarejo ma 7 linii autobusowych , z których 3 komunikują się bezpośrednio ze stolicą Madrytu , docierając do centralnej Ronda de Atocha .
Pozostałe cztery linie łączą Villarejo z innymi miastami w okolicy .
