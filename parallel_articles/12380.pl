Villavicencio
Villavicencio – miasto w środkowej Kolumbii , położone nad rzeką Guatiquía , na przedgórzu Kordyliery Wschodniej ( Andy Północne ) .
Jest ośrodkiem administracyjnym departamentu Meta .
Według spisu ludności z 30 czerwca 2018 roku miasto liczyło 492 052 mieszkańców .
Charakterystyka .
Villavicencio jest ośrodkiem handlowym regionu rolniczego i usługowo-gospodarczego wschodniej części kraju .
