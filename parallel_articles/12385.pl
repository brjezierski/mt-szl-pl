Villemaur-sur-Vanne
Villemaur-sur-Vanne – miejscowość i dawna gmina we Francji , w regionie Grand Est , w departamencie Aube .
W dniu 1 stycznia 2016 roku z połączenia trzech ówczesnych gmin – Aix-en-Othe , Palis oraz Villemaur-sur-Vanne – powstała nowa gmina Aix-Villemaur-Pâlis .
W 2013 roku populacja Villemaur-sur-Vanne wynosiła 512 mieszkańców .
