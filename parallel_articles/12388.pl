Villeneuve-sur-Lot
Villeneuve-sur-Lot – miejscowość i gmina we Francji , w regionie Nowa Akwitania , w departamencie Lot i Garonna .
W 2013 roku populacja gminy wynosiła 24 481 mieszkańców .
Przez teren gminy przepływa rzeka Lot .
Gmina Villeneuve-sur-Lot sąsiaduje z Bias , Pujols i La Sauvetat-sur-Lède .
Miejscowość znajduje się w odległości 24 km na północny wschód od Agen – jednego z większych miast w pobliżu .
