Vilvoorde
Vilvoorde ( franc .
`` Vilvorde '' ) – miasto w północnej Belgii , w prowincji Brabancja Flamandzka .
W 2008 r. liczyło 38 557 mieszkańców .
W mieście rozwinął się przemysł chemiczny , materiałów budowlanych , włókienniczy , ceramiczny , papierniczy oraz spożywczy .
W Vivoorde urodziła się Alison Van Uytvanck , belgijska tenisistka .
