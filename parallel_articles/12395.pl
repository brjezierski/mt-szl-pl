Vimmerby
Vimmerby – miasto i siedziba gminy Vimmerby w regionie Kalmar południowo-wschodniej Szwecji .
Literatura .
W Vimmerby dnia 14 listopada 1907 urodziła się pisarka Astrid Lindgren .
W miasteczku rozgrywają się jej niektóre powieści dla dzieci , przede wszystkim `` Pippi Pończoszanka '' .
Pierwowzorem osady z powieści `` Dzieci z Bullerbyn '' jest pobliska wieś Sevedstorp , w której pisarka spędziła dzieciństwo i gdzie nadal można obejrzeć trzy zagrody opisane w książce .
Natomiast seria książek `` Emil ze Smalandii '' , która była inspirowana dzieciństwem jej ojca , rozgrywa się w pobliskiej wsi Lönneberga i jej okolicach , w tym również w samym miasteczku .
Pisarka opisała życie swoich rodziców w książce `` Samuel August z Sevedstorp i Hanna w Hult '' .
Na północno-zachodnim krańcu miasta przy Fabriksgatan znajduje się park rozrywki `` Astrid Lindgrens Värld '' poświęcony twórczości pisarki .
