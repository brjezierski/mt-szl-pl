Vindefontaine
Vindefontaine – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku jej populacja wynosiła 332 mieszkańców .
1 stycznia 2016 roku połączono sześć wcześniejszych gmin : Amfreville , Cretteville , Gourbesville , Houtteville , Picauville oraz Vindefontaine .
Siedzibą gminy została miejscowość Picauville , a nowa gmina przyjęła jej nazwę .
