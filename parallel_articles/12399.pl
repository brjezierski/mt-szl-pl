Naparstniczka czeska
Naparstniczka czeska ( `` Verpa bohemica '' ( Krombh . )
J .
Schröt . )
– gatunek grzybów z rodziny smardzowatych ( `` Morchellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Morchellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1834 Julius Vincenz von Krombholz nadając mu nazwę `` Morchella bohemica '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1893 Joseph Schröter , przenosząc go do rodzaju `` Verpa '' .
