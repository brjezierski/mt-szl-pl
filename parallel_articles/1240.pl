Anzelm Ephorinus
Anzelm Ephorinus ( Anzelm Eforyn , Eforinus ) ( ur .
? , zm .
1566 ) – śląski humanista , lekarz .
Życiorys .
Anzelm Ephorinus urodził się w Mirsku nad Kwisą na Śląsku .
Studiował na Akademii Krakowskiej , w której następnie wykładał dialektykę i logikę .
W Padwie otrzymał tytuł doktora medycyny .
Po powrocie do Polski został mianowany pierwszym lekarzem miasta Krakowa .
Korespondował z Erazmem z Rotterdamu .
Autor kilku dzieł .
Według środowisk działających na rzecz uznania narodowości śląskiej jest jednym z protoplastów narodu śląskiego , bowiem kiedy korespondował z Erazmem z Rotterdamu , tamten wielokrotnie zwracał się do niego jak do Polaka , na co Anzelm , podpisując się w liście , napisał : „ Tuus et suus , Anselmus Ephorinus , Silesius non Polonus ” ( „ Twój i swój własny , Anzelm Eforyn , Ślązak nie Polak ” )
