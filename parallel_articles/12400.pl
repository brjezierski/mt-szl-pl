Mleczaj pomarszczony
Mleczaj pomarszczony ( `` Lactarius pterosporus '' Romagn . )
– gatunek grzybów z rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lactarius , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podała Alina Skirgiełło w 1998 r. Synonimy naukowe :
Morfologia .
Średnica 4–8 ( 10 ) cm , początkowo wypukły , potem przeważnie płaski z niewielką wklęsłością na środku , w której często występuje stożkowaty garbek .
Jest pomarszczony .
Brzeg podwinięty tylko u młodych okazów , potem rozprostowuje się .
W stanie suchym powierzchnia sucha , matowa , nieco aksamitna , miejscami jednak gładka .
W stanie wilgotnym nieco śluzowaty .
