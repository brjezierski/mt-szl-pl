Vinkovci
Vinkovci – miasto w Chorwacji , w żupanii vukowarsko-srijemskiej , siedziba miasta Vinkovci .
Leży we wschodniej Slawonii .
W 2011 roku liczyło 32 029 mieszkańców .
Większość mieszkańców miasta stanowią Chorwaci ( 88,99 % ) .
Vinkovci leżą na jednym z brzegów rzeki Bosut , 19 km na południowy zachód od Vukovaru , 24 km na północ od Županji oraz 43 km na południe od Osijeku .
