Virgin Gorda
Virgin Gorda – trzecia pod względem wielkości wyspa Brytyjskich Wysp Dziewiczych , o powierzchni 21 km² .
Wyspę zamieszkuje 3063 mieszkańców ( 2003 ) .
Najwyższym szczytem jest Virgin Peak ( 414 m n.p.m . ) .
Głównym miastem na wyspie jest Spanish Town położone w jej południowo-zachodniej części .
Niektóre inne miejscowości wyspy : Creek Village .
