Virginia Beach
Virginia Beach – miasto w Stanach Zjednoczonych , największe w stanie Wirginia , nad Oceanem Atlantyckim , w obszarze metropolitalnym Virginia Beach–Norfolk–Newport News .
Według spisu z 2020 roku liczy 459,5 tys. , oraz 1,8 mln mieszkańców w obszarze metropolitalnym .
Miasto jest ważnym ośrodkiem turystycznym i znane jako tętniące życiem nadmorskie miejsce wypoczynkowe , sportowe i biznesowe .
W 2018 roku przyciągnęło ponad 20 mln odwiedzających .
Virginia Beach jest także domem dla kilku baz wojskowych Stanów Zjednoczonych .
Historia .
Virginia Beach została założona w 1887 roku i rozwijała się jako kurort dzięki budowie hoteli i linii kolejowej łączącej miasto z Norfolk .
Po I wojnie światowej stał się ważną bazą w narodowym systemie obrony wybrzeża .
