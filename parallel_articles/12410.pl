Visp
Visp ( fr .
`` Viège '' ) – miasto i gmina polityczna w południowej Szwajcarii , w kantonie Valais , u wylotu doliny Vispertal .
Miasto słynie z fabryki Lonza ( szwajcarska firma chemiczna i biotechnologiczna ) , otworzonej w 1907 roku , która pozostaje najważniejszym pracodawcą w mieście , zatrudniając łącznie ponad 3 tys .
pracowników .
Urodzeni w Vispie .
6 sierpnia 1969 , podczas urlopu w Vispie , zmarł współtwórca szkoły frankfurckiej , filozof niemiecki Theodor W. Adorno .
21 kwietnia 1991 w Vispie zmarł na udar mózgu austriacki skrzypek i dyrygent Willi Boskovsky .
