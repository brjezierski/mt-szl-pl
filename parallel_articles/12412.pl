Piestrzyca kędzierzawa
Piestrzyca kędzierzawa ( `` Helvella crispa '' ( Scop . )
Fr . )
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1772 r. Giovanni Antonio Scopoli nadając mu nazwę `` Phallus crispus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1822 r. Elias Fries , przenosząc go do rodzaju `` Helvella '' .
