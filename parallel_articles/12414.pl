Vitoria-Gasteiz
Vitoria-Gasteiz – miasto w północnej Hiszpanii , stolica prowincji Álava i wspólnoty autonomicznej Kraju Basków .
Około 242 tysięcy mieszkańców w 2012 roku .
Vitoria-Gasteiz była Zieloną Stolicą Europy w 2012 roku .
Historia .
W 581 Leowigild , władca Wizygotów , założył miasto `` Victoriacum '' dla uczczenia zwycięstwa nad Baskami .
Na jego miejscu w 1181 Sancho VI Mądry , król Nawarry , założył miasto pod nazwą `` Nova Victoria '' .
Od 1199 miasto przeszło w posiadanie Kastylii , gdy po dziewięciomiesięcznym oblężeniu zdobył je Alfons VIII Szlachetny .
Pod panowaniem kastylijskim miasto wzbogaciło się na handlu wełną .
