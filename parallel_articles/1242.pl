Antananarywa
Antananarywa ( `` Antananarivo '' , potocznie `` Tana '' ) – stolica Madagaskaru i prowincji Antananarywa .
Położona jest w środkowej części wyspy , nad rzeką Ikopa , na wysokości ok. 1400 m n.p.m .
Antananarywa jest największym miastem na wyspie , stanowiącym centrum administracyjne , gospodarcze , naukowe i komunikacyjne kraju .
Rozwinął się tu przemysł spożywczy , tytoniowy i włókienniczy .
Znajdują się tu wyższe uczelnie University of Madagascar oraz Collège Rural d'Ambatobe .
Nazwa miasta .
Nazwa miasta `` Antananarywa '' oznacza „ Miasto Tysiąca '' .
W okresie kolonialnym i przez kilka lat po odzyskaniu niepodległości ( do 1976 ) używano formy Tananarywa ( `` Tananarive '' ) .
