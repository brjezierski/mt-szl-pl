Vlissingen
Vlissingen – gmina i miasto w południowo-zachodniej części Holandii , położone na dawnej wyspie Walcheren ( prowincja Zelandia ) .
Liczy 42 864 mieszkańców ( 2012 ) .
Vlissingen otrzymało prawa miejskie w 1315 .
W XVII stuleciu było główną bazą dla statków Kompanii Wschodnioindyjskiej i ważnym portem .
Obecnie Vlissingen jest znane głównie z przystani Holenderskiej Marynarki Wojennej , położonej nad brzegami Skaldy .
Oprócz tego znajduje się tu stocznia Damen Schelde Naval Shipbuilding ( poprzednia nazwa : Koninklijke Maatschappij De Schelde ) , rafineria , przetwórnie rybne , huta aluminium .
Vlissingen dysponuje połączeniem kolejowym i drogowym z Bredą oraz promowym z Breskens ( Zelandia ) .
Połączenie promowe do Sheerness ( Wielka Brytania ) zostało zlikwidowane w maju 1994 roku .
W okolicach Vlissingen działa elektrownia atomowa `` Borsele '' o mocy 477 MW .
Z ważniejszych atrakcji turystycznych można wymienić : kościół `` Grote Kerk '' ( z XIV w. ) , wieżę Więzienną ( koniec XVI w. ) , barokowe kamienice ( XVII w. ) , ratusz miejski ( XVIII w. ) , muzeum morskie , twierdza `` Fort Rammekens '' ( XVI w. ) .
Miasto posiada liczne kąpieliska oraz przystań jachtową .
