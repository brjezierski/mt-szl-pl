Wlora ( alb .
`` Vlorë '' , `` Vlora '' ; wł .
`` Valona '' , gr .
`` Βαλόνα '' ) – miasto w Albanii na wybrzeżu Adriatyku .
Liczba mieszkańców około 125 tysięcy .
Stolica okręgu i obwodu Wlory .
Miasto nadmorskie , nad którym wznosi się wzgórze Kuzum-Baba – siedziba muzułmańskiej sekty bektaszytów .
W 1912 Wlora stała się miejscem spotkania albańskich działaczy narodowych , którzy proklamowali deklarację niepodległości .
W czasie I wojny światowej miasto zostało zajęte przez jednostki armii włoskiej .
W 1920 , w wyniku bitwy o Wlorę , oddziały albańskie odzyskały miasto z rąk włoskich .
W 1997 r. , po bankructwie piramid finansowych miasto zostało przejęte przez uzbrojone bandy , które wystąpiły przeciwko legalnym władzom .
Znane też jako największy ośrodek przemytu ludzi do Włoch .
Od 2008 burmistrzem miasta jest Shpetim Gjika .
