Voorburg
Voorburg - holenderskie miasteczko i dawna gmina w zachodniej części prowincji Holandia Południowa , w gminie Leidschendam-Voorburg .
Posiada około 39 000 mieszkańców .
Jest najstarszą miejscowością w Holandii - w 1988 świętował 2000 lat istnienia .
Do 2002 było osobną gminą .
Transport .
W Voorburgu znajduje się stacja kolejowa .
Dawniej były trzy , jednakże Voorburg 't Loo i Leidschendam-Voorburg stały się częścią RandstadRail .
Sport .
W Voorburgu znajduje się stadion do krykieta - Sportpark Westvliet , na którym gra klub VCC ( Voorburg Cricket Club ) .
Stadion ten otrzymał status ODI , który pozwala organizować tzw .
jednodniowe mecze międzynarodowe .
Rozegrano na nim mecz Bangladesz - Szkocja w formule Twenty20 International ( do 20 overów ) .
