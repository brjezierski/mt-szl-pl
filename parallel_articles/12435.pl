Vouziers
Vouziers – miejscowość i gmina we Francji , w regionie Grand Est , w departamencie Ardeny .
1 stycznia 2016 roku połączono trzy wcześniejsze gminy : Terron-sur-Aisne , Vouziers oraz Vrizy .
Siedzibą gminy została miejscowość Vouziers , a nowa gmina przyjęła jej nazwę .
W 2013 roku populacja Vouziers wynosiła 4456 mieszkańców .
W Vouziers w 1828 urodził się Hippolyte Taine , francuski filozof , psycholog , historyk sztuki i literatury oraz krytyk literacki .
