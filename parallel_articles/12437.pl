Vracov ( przystanek kolejowy )
Vracov – przystanek kolejowy w miejscowości Vracov , w kraju południowomorawskim , w Czechach .
Znajduje się na wysokości 195 m n.p.m .
Jest zarządzany przez Správę železnic .
Na przystanku znajdują się kasy biletowe , na których istnieje możliwość zakupu biletów na wszystkie pociągi w tym międzynarodowe oraz rezerwacji miejsc .
