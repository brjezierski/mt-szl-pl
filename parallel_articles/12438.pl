Vrahovice
Vrahovice ( niem .
`` Wrahowitz '' ) – dzielnica miasta Prościejów w Czechach , na Morawach , w kraju ołomunieckim .
W 2001 roku miasto liczyło 3402 mieszkańców .
Wzmiankowane jako wieś po raz pierwszy w 1337 .
W Vrahovicach urodzili się muzycy Zdeněk Tylšar i Bedřich Tylšar .
Językoznawca i slawista František Kopečný mieszkał tu przez wiele lat .
Piłkarz Rostislav Václavíček urodził w Vrahovicach i grał w piłkę nożną w miejscowej drużynie na początku swej kariery .
