Vrbové
Vrbové – miasto na Słowacji w kraju trnawskim , liczy około 6,3 tys .
mieszkańców ( 2011 ) .
Położone około 10 km od uzdrowiskowej miejscowości Pieszczany .
Vrbové nazywane jest słowacką Pizą ze względu na krzywą wieżę jednego z tamtejszych kościołów .
W centrum znajduje się okazała synagoga , ponieważ miasto zamieszkiwała liczna społeczność żydowska .
W Vrbovem urodził się żołnierz , uczestnik konfederacji barskiej , zesłaniec na Kamczatkę , skąd dokonał brawurowej ucieczki w roku 1772 , pochodzenia polsko-węgierskiego hrabia Maurycy Beniowski , który został królem Madagaskaru .
