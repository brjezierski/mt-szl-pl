Vrútky
Vrútky ( do 1927 `` Dolné Vrútky '' i `` Horné Vrútky '' ; niem .
`` Ruttek '' , węg .
`` Ruttka '' ) – miasto na Słowacji , w kraju żylińskim , w powiecie Martin , liczy 7539 mieszkańców ( spis ludności z 2011 ) .
Położenie .
Położone są w północnej części Kotliny Turczańskiej , u południowych podnóży Małej Fatry , u ujścia Turca do Wagu , na wysokości 384 m n.p.m .
Historia .
Teren dzisiejszych Vrútek był zamieszkany już w okresie kultury puchowskiej .
W okresie najazdu tatarskiego w latach 1241–1242 w ochronie zamku Zniev zasłużyli się również mieszkańcy Vrútek , za co 15 z nich ( w tym Ruttkayowie ) zostało podniesionych przez króla Belę IV do stanu szlacheckiego i obdarowanych ziemią w Kotlinie Turczańskiej .
