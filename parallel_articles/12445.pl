Vuilleminia alni
Vuilleminia alni Boidin , Lanq .
& amp ; Gilles – gatunek grzybów z rodziny `` Vuilleminiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Vuilleminia , Vuilleminiaceae , Corticiales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1994 r. Jacques Boidin , Paule Lanquetin i Gérard Gilles na martwych gałęziach olszy czarnej ( `` Alnus glutinosa '' ) we Francji .
Synonim : `` Vuilleminia coryli '' var .
