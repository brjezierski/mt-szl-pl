Vukovar
Vukovar – miasto we wschodniej Chorwacji , w Slawonii , stolica żupanii vukowarsko-srijemskiej , siedziba miasta Vukovar .
Miasto leży u ujściu Vuki do Dunaju , w pobliżu granicy z Serbią .
Miasto jest znane z przemysłu włókienniczego ( produkcja pończoch ) i spożywczego .
Ponadto w Vukovarze mieści się port rzeczny .
W 2011 roku liczyło 26 468 mieszkańców .
