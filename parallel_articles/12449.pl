Wysokie Tatry
Wysokie Tatry ( ) – miasto położone w słowackich Tatrach , obejmujące znaczną część tych gór .
Jest drugim miastem na Słowacji pod względem powierzchni .
Założone zostało w 1947 roku i ma ok. 4200 stałych mieszkańców .
Składa się z 15 części – pierwotnie samodzielnych osiedli , które stopniowo integrowały się w całość .
Wysokie Tatry dwukrotnie organizowały mistrzostwa świata w narciarstwie klasycznym : w 1935 i 1970 roku .
