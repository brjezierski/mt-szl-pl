Anthony ( Teksas )
Anthony – miasto w Stanach Zjednoczonych , w stanie Teksas , w hrabstwie El Paso .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 miasto liczyło 5 011 mieszkańców , co oznacza wzrost o 30,2 % w porównaniu do roku 2000 .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 72,0 % , Afroamerykanie 9,2 % , Azjaci 0,9 % , pozostali 17,9 % .
