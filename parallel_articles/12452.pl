Vágar
Vágar ( wym .
, inaczej `` Vágoy '' , duń .
`` Vågø '' ) – wyspa oceaniczna pochodzenia wulkanicznego , należąca do archipelagu Wysp Owczych , który mieści się na Morzu Norweskim .
Ma 177,16 km² ; co daje jej 3. miejsce pod względem wielkości .
Mieszka na niej 2987 osób , a na jeden km² przypada 16,9 osoby .
Geografia .
Na wyspie znajdują się dwa największe jeziora całego archipelagu : największe , Sørvágsvatn , mające 3,56 km² ; a także drugie pod względem wielkości Fjallavatn ( 1,02 km² ) .
Do systemu wodnego wyspy zalicza się także wiele cieków wodnych ze źródłami w górach lub wypływających z Fjallavatn , wokół którego znajduje się największy teren podmokły całego archipelagu .
Z jeziora Sørvágsvatn wpada do oceanu wysoki , 45-metrowy wodospad Bøsdalafossur .
Od strony północno-wschodniej teren wyspy jest dość niski , w przeciwieństwie do strony północno-zachodniej .
Najwyższym punktem jest szczyt góry Árnafjall ( pol .
`` Orla Góra '' ) , która ma 722 m n.p.m. , zaraz po niej jest Eysturtindur , niższa jedynie o 7 m. Prócz nich na Vágarze jest 39 gór .
Kształt wyspy przypomina łeb psa , gdzie fjord Sørvágsfjørður jest rozwartym pyskiem , a jezioro Fjallavatn okiem .
Położenie .
Vágar znajduje się w zachodniej części Wysp Owczych .
Od południa i północnego zachodu otacza wyspę Ocean Atlantycki , na północnym wschodzie i wschodzie jest odgrodzona od wyspy Streymoy cieśniną Vestmannasund , biorącą swą nazwę od miasta leżącego po drugiej stronie .
Od zachodu , gdzie cieśnina Mykinesfjørður odgradza ją od wyspy Mykines , z którą razem składają się na administracyjny region Vágar .
Wysepki i skały .
Wokół Vágar znajdują się liczne wysepki oraz szkiery usytuowane głównie na zachodzie w okolicach fiordu Sørvágsfjørður .
Największą jest Tindhólmur , której powierzchnia to ok. 65 ha .
Składa się z siedmiu szczytów , z których najwyższy sięga 262 m n.p.m .
Ponoć kiedyś mieszkała tam rodzina składająca się z mężczyzny , kobiety i jednego dziecka .
Mieszkańcy szybko ją jednak opuścili , kiedy mieszkający na szczycie orzeł skradł i zabił dziecko .
Od tamtej pory Tindhólmur ma pozostać niezamieszkany .
Gáshólmur jest drugi w tej kolejności , którego rozmiar to ok. 10 ha , a najwyższy punkt znajduje się na wysokości 65 m n.p.m .
Jego jedynymi mieszkańcami są ptaki oraz owce .
