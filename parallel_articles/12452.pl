Historia .
Vágar jest jedną z najwcześniej skolonizowanych wysp wchodzących w skład archipelagu .
Nic nie wiadomo na temat obecności wcześniejszych ludów na tym obszarze , pierwszymi z nich mogli więc być wikingowie , przypływający na Wyspy Owcze głównie z Norwegii .
Pierwszą z miejscowości było najprawdopodobniej miasteczko Sørvágur , które pierwotnie powstało bardziej na północ w stosunku do dzisiejszego centrum osady , a jego początki datuje się na IX/X wiek .
Później dopiero uznano , że tereny w tamtych okolicach są mniej korzystne od tych leżących na południu i postanowiono się tam przenieść opuszczając domostwa .
Prawdopodobnie dzięki temu odkryto , iż osada nie pochodzi jak sądzono z okresu przełomu XV i XVI wieku , nie wspomniało bowiem o niej jedno z bardzo niewielu źródeł mówiących o archipelagu – islandzka saga , Faeringa Saga .
Ruiny domostw odnaleziono w 1957 , kiedy budowano szkołę .
Następna wioska powstała dwa wieki później , na południowym wschodzie .
Nazwano ją Sandavágur , co po przetłumaczeniu na język polski oznacza „ `` Piaszczysty Strumień '' ” .
Świadectwem wikińskiej przeszłości tej miejscowości jest kamień runiczny odnaleziony w 1918 , w którym Torkil Onundarson żyjący w XII wieku stwierdził , że jest pierwszym mieszkańcem tej ziemi .
Trzecia osada , Bøur powstała na przełomie XIII i XIV wieku , a pierwszym świadectwem na jej istnienie jest „ `` Psi List '' ” .
Ulokowano ją w pobliżu Sørvágur .
Prawdopodobnie w tym momencie mieszkańcy Sørvágur przenosili się już na południe pomiędzy rzeki Stórá i Kirkjuá .
Niewiele po Bøur zaczęła rozwijać się Gásadalur , założona najprawdopodobniej około XIV wieku , na zachodnim krańcu wyspy odcinając się od pozostałych osad łańcuchami górskimi .
Kolejną XIV-wieczną osadą jest Miðvágur , dziś największa osada wyspy .
Zbudowano ją po drugiej stronie jeziora Sørvágsvatn .
Na tych zakończyła się wczesna , średniowieczna kolonizacja Vágar .
Cały XV i XVII wiek , aż do 1665 wioska była własnością rodziny Benkenstocków , którzy należeli do norweskiego możnowładztwa , wykupił je jednak od nich Severin Fohrman , który postanowił odsprzedać miejscowej ludności cały teren .
W XVII wieku ludzie ostatecznie opuszczają pierwsze miejsce skolonizowane jeszcze przez wikingów .
XIX wiek niósł za sobą kilka nowości dla wyspy Vágar .
Wtedy szczyt swej twórczości osiągnął Jens Christian Svabo , etnograf i lingwista , który całe swe życie spędził na badaniu i spisaniu przekazywanej ustnie z pokolenia na pokolenie tradycji farerskiej .
W tym czasie w miejscowości Sørvágur urodził się też Gamli Dávur , pierwszy człowiek , który miał własną łódź rybacką bez konieczności posiadania dóbr ziemskich .
