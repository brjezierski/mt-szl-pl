Växjö
Växjö ( szw .
[ ] ) – miasto ( `` tätort '' ) w południowej Szwecji .
Siedziba władz ( `` residensstad '' ) regionu administracyjnego ( `` län '' ) Kronoberg .
Ośrodek administracyjny ( `` centralort '' ) gminy Växjö oraz siedziba władz diecezjalnych i biskupa diecezji Växjö .
Do 1970 Växjö miało administracyjny status miasta .
Rodzinne miasto pisarza Para Lagerkvista .
W 2010 Växjö liczyło 60 887 mieszkańców .
