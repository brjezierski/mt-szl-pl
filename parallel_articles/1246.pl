Anthostoma decipiens ( DC . )
Nitschke– gatunek grzybów z rodziny `` Diatrypaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Anthostoma , Diatrypaceae , Xylariales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1805 r. Augustin Pyramus de Candolle , nadając mu nazwę `` Sphaeria decipiens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1867 r. Theodor Rudolph Joseph Nitschke .
Synonimy :
