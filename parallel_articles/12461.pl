Waalwijk ( wym . )
– gmina i miasto położone jest na południu Holandii , w prowincji Brabancja Północna .
Populacja wynosi 46 525 w ( 2014 ) .
Miasto posiada jeden z najbardziej zadziwiających kościołów w Holandii - `` Sint Janskerk '' ( Kościół św .
Jana ) .
Z Waalwijk pochodzi klub piłkarski RKC Waalwijk
Miasto Waalwijk jest dobrze znane przede wszystkim z przemysłu obuwniczego .
