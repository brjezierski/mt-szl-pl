Waconia ( Minnesota )
Waconia – miasto w Stanach Zjednoczonych , w stanie Minnesota , w hrabstwie Carver .
Geografia .
Miasto graniczy z południowym brzegiem jeziora Waconia , które jest drugim jeziorem w obszarze metropolitalnym 7 hrabstw pod względem wielkości .
Jest jeziorem słodkowodnym , którego powierzchnia wynosi 3200 akrów .
