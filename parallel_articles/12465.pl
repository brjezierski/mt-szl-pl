Wadim Abdraszytow
Wadim Abdraszytow ( ros .
Вадим Юсупович Абдрашитов , tatar .
Вадим Йосыф улы Габдерәшитов , `` Vadim Yosıf uğlı Ğabderəşitov '' ; ur .
19 stycznia 1945 w Charkowie , zm .
12 lutego 2023 ) – rosyjski reżyser filmowy pochodzenia tatarskiego , także pedagog .
Życiorys .
W latach 1959–1961 uczył się w technikum transportu kolejowego w Ałma-Acie , a następnie , w latach 1961–1964 pobierał nauki w Moskiewskim Instytucie Fizyczno-Technicznym .
W 1967 ukończył tamtejszy Instytut Chemiczno-Technologiczny .
W trakcie studiów zainteresował się filmem .
Hobbystycznie zajmował się dokumentowaniem życia uczelni .
W latach 1967–1970 był zatrudniony na stanowisku inżyniera w fabryce chemicznej , nie przestał jednak myśleć o pracy filmowca .
W 1974 został absolwentem wydziału reżyserii Państwowego Instytutu Kinematografii ( WGIK ) ( w którym obecnie wykłada ) .
Uczył się tam m.in .
od Michaiła Romma i Lwa Kulidżanowa .
W czasie studiów zrealizował dwie etiudy : `` Reportaż z asfaltu '' ( `` Rieportaż s asfalta '' ) i `` Zatrzymajcie Potapowa '' ( `` Ostanowitie Potapowa '' ) , które zdobyły nagrody na przeglądach filmów szkolnych .
Był reżyserem w wytwórni Mosfilm .
Często współpracował z pisarzem i scenarzystą Aleksandrem Mindadze .
Tworzył dramaty społeczne i obyczajowe , często o charakterze polemicznym , w których na ogół porusza problematykę społeczeństwa radzieckiego lat 80 .
