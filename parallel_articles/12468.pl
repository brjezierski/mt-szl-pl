Historia .
Wadżda została założona w X wieku przez Berberów .
Na przestrzeni wieków była przedmiotem licznych sporów terytorialnych i przechodziła pod panowanie wielu mocarstw i dynastii .
W XIII i XIV wieku była okupowana przez Abdalwadydów z Tilimsanu , a od 1727 roku do początków XIX wieku jako jedyne miasto współczesnego Maroka znajdowała się pod panowaniem Turcji osmańskiej .
Na przełomie XIX i XX wieku miasto było dwukrotnie zdobywane przez Francuzów – po raz ostatni w 1912 roku , kiedy na stałe przyłączono je do francuskiego protektoratu .
W późniejszych latach , ze względu na odległość od władz w Rabacie i bliskość algierskiej granicy Wadżda cieszyła się opinią miasta niespokojnego i dysydenckiego – na początku lat 60. doszło tu nawet do wojny przygranicznej , a w latach 80. przez miasto przetoczyła się fala strajków studenckich .
Krótkotrwały rozkwit miasta nastąpił po przywróceniu przez Maroko stosunków dyplomatycznych i otwarciu granicy z Algierią w 1988 roku , jednak wymianę handlową i kulturową skutecznie zatrzymało ponowne zamknięcie granicy po wybuchu wojny domowej w Algierii .
