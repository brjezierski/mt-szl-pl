Anthracobia macrocystis
Anthracobia macrocystis ( Cooke ) Boud .
– gatunek grzybów z rodziny `` Pyronemataceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Anthracobia , Pyronemataceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1873 roku Mordecai Cubitt Cooke nadając mu nazwę `` Peziza subhirsuta '' var .
`` macrocystis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1867 roku Jean Louis Émile Boudier .
