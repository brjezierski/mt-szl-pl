Anthracobia melaloma ( Alb .
& amp ; Schwein . )
Arnould – gatunek grzybów z rodziny `` Pyronemataceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Anthracobia , Pyronemataceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisali w roku 1805 Johannes Baptista von Albertini i Lewis David von Schweinitz nadając mu nazwę `` Peziza melaloma '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1893 roku Léon Arnould .
