Niektóre z nich :
Morfologia .
Owocniki typu apotecjum występujące pojedynczo lub w grupach .
Początkowo mają typowy kształt miseczki , później rozpłaszczają się .
Brzeg z ciemnobrązowymi , bardzo krótkimi włoskami ( długość do 30 µm ) , grubość do 15 µm .
Powierzchnia hymenialna żółto-pomarańczowo-różowa .
Zarodniki eliptyczne z dwoma dużymi gutulami .
Podobna jest `` Anthracobia maurilabra '' .
Odróżnia się ciemnobrązowymi , dłuższymi ( 120-130 µm ) i cieńszymi ( do 8 µm ) włoskami o nieco rozszerzonym wierzchołku i spłaszczonej , nie maczugowatej podstawie .
Występowanie i siedlisko .
`` Anthracobia melaloma '' występuje w Ameryce Północnej , Europie , Australii i na Nowej Zelandii .
W Polsce po raz pierwszy jej występowanie podał Bogumir Eichler w 1902 r. , później gatunek notowany jeszcze wielokrotnie .
