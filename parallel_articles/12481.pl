Waldemar Fornalik
Waldemar Józef Fornalik ( ur .
11 kwietnia 1963 w Myślenicach ) – polski piłkarz i trener piłkarski , w latach 2012–2013 selekcjoner reprezentacji Polski .
Od 29 listopada 2022 roku trener Zagłębia Lubin .
Kariera zawodnicza .
Podczas swej kariery zawodniczej występował jako obrońca .
Jest wychowankiem Ruchu Chorzów , w którym grał przez całą karierę .
W I lidze zadebiutował w wieku dwudziestu lat w wyjazdowym meczu ze Stalą Mielec .
Ówczesny trener Ruchu Orest Lenczyk wystawił go na pozycji defensywnego pomocnika .
W latach 1983–1994 rozegrał w ekstraklasie 233 spotkania , zdobywając w nich cztery gole .
W 1989 zdobył z Ruchem tytuł mistrza Polski .
Kariera trenerska .
Posiada licencję UEFA Pro Licence , uprawnienia trenera I klasy , ukończył Akademię Wychowania Fizycznego w Katowicach .
Trzykrotnie w plebiscycie tygodnika „ Piłka Nożna ” został uznany za trenera roku ( 2009 , 2012 , 2019 ) .
Jako trener zadebiutował w ekstraklasie 18 października 1997 , prowadząc zespół Ruchu w meczu z Wisłą Kraków ( 1:0 ) podczas nieobecności szkoleniowca Niebieskich Oresta Lenczyka , przebywającego wówczas za granicą ( Fornalik był jego asystentem ) .
Kariera klubowa .
Jako trener pracował w Ekstraklasie w Górniku Zabrze i Odrze Wodzisław Śląski .
W Odrze zastąpił na stanowisku pierwszego trenera Franciszka Smudę .
W sezonie 2005/06 Odra zajęła szóste miejsce w ekstraklasie .
Następnie trenował m.in .
Polonię Warszawa i Widzewa Łódź .
27 kwietnia 2009 zastąpił Bogusława Pietrzaka na stanowisku trenera Ruchu Chorzów .
W sezonie 2011/2012 zdobył z nim wicemistrzostwo Polski i doprowadził do finału Pucharu Polski .
Wśród kibiców chorzowskiej drużyny otrzymał przydomek `` Waldek King '' .
7 maja 2012 roku otrzymał nagrodę trenera sezonu Ekstraklasy .
10 lipca 2012 zrezygnował z pracy w Ruchu zostając selekcjonerem reprezentacji Polski .
7 października 2014 ponownie został I trenerem Ruchu Chorzów , przejmując go od Jána Kociana .
Ponownie odszedł z tego klubu 22 kwietnia 2017 po przegranym meczu z Lechem Poznań ( 0:3 ) .
Zostawił wówczas Ruch na czternastym miejscu z tym samym dorobkiem punktowym , co drużyny będące na miejscach spadkowych .
Ostatecznie w tamtym sezonie Ruch spadł z Ekstraklasy .
We wrześniu 2017 Waldemar Fornalik zastąpił Dariusza Wdowczyka na stanowisku trenera Piasta Gliwice .
Jego asystentem jest jego młodszy brat Tomasz .
W sezonie 2018/19 zdobył z Piastem mistrzostwo Polski .
W Piaście pracował do 27 października 2022 .
29 listopada 2022 został trenerem Zagłębia Lubin , podpisał kontakt do 30 czerwca 2024 .
Selekcjoner reprezentacji Polski .
10 lipca 2012 został przez Polski Związek Piłki Nożnej wybrany selekcjonerem reprezentacji Polski .
Prowadził ten zespół podczas eliminacji do mistrzostw świata 2014 , w których Polska zajęła czwarte miejsce w swojej grupie , nie uzyskując awansu .
16 października 2013 , dzień po przegranym meczu z Anglią ( 0:2 ) został odwołany z funkcji selekcjonera .
Pod jego wodzą drużyna narodowa rozegrała osiemnaście spotkań ( osiem zwycięstw , cztery remisy , sześć porażek ) .
Życie prywatne .
Brat Tomasza Fornalika , polskiego piłkarza i trenera .
