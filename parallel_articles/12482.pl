Waldemar Sobota
Waldemar Paweł Sobota ( ur .
19 maja 1987 w Ozimku ) – polski piłkarz i futsalista , występujący na pozycji pomocnika , w latach 2011–2014 reprezentant Polski .
Od 2022 zawodnik futsalowego klubu Dreman Opole Komprachcice .
Posiada również obywatelstwo niemieckie ( jego ojciec był działaczem mniejszości niemieckiej ) , określa się jako Ślązak polskiej narodowości .
Kariera klubowa .
Sobota pochodzi ze wsi Schodnia w gminie Ozimek .
Piłkarską karierę rozpoczął w Małejpanwi Ozimek , w której w rozgrywkach IV ligi zdobył czternaście bramek .
W wieku 18 lat zdecydował się na przejście do KS Krasiejów , w którego barwach strzelił w IV lidze 21 goli .
W 2007 został zawodnikiem trzecioligowego MKS Kluczbork .
Z klubem tym w sezonie 2008/2009 zajął pierwsze miejsce w II lidze ( trzeci poziom ) i wywalczył awans do I ligi .
Po zakończeniu sezonu 2009/2010 został przez tygodnik „ Piłka Nożna ” wybrany do jedenastki sezonu I ligi oraz otrzymał tytuł odkrycia rozgrywek .
Dzięki udanym występom na drugim poziomie rozgrywkowym Sobotą zainteresowało się kilka klubów z Ekstraklasy , m.in .
Górnik Zabrze i GKS Bełchatów .
Najbardziej konkretny okazał się jednak Śląsk Wrocław , z którym 9 czerwca 2010 zawodnik podpisał czteroletni kontrakt .
12 sierpnia 2010 zadebiutował w Ekstraklasie w spotkaniu z Jagiellonią Białystok .
Dzięki dobremu występowi kibice Śląska wybrali go na najlepszego zawodnika tego spotkania , a niektóre media umieściły go w jedenastce kolejki .
Na początku września tygodnik „ Piłka Nożna ” umieścił go w najlepszej jedenastce sierpnia .
Również wrocławscy kibice nagrodzili go tytułem najlepszego gracza Śląska w sierpniu .
18 września 2010 w spotkaniu z Widzewem Łódź Sobota zdobył swoją pierwszą bramkę w Ekstraklasie .
Ze Śląskiem w sezonie 2010/2011 zdobył wicemistrzostwo Polski , a w sezonie 2011/2012 wywalczył mistrzostwo oraz Superpuchar Polski .
