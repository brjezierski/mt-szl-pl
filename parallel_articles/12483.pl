Waldshut-Tiengen
Waldshut-Tiengen – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Fryburg , siedziba regionu Hochrhein-Bodensee , powiatu Waldshut , oraz wspólnoty administracyjnej Waldshut-Tiengen .
Leży nad górnym Renem , na skraju południowego Schwarzwaldu , przy granicy ze Szwajcarią .
Dzielnice .
Miasto dzieli się na dziesięć dzielnic : Aichen-Gutenburg , Breitenfeld , Detzeln , Eschbach , Gaiß-Waldkirch , Gurtweil , Indlekofen , Krenkingen , Oberalpfen , Schmitzingen .
