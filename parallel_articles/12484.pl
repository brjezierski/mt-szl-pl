Walentina Matwijenko
Walentina Iwanowna Matwijenko ( ros .
Валентина Ивановна Матвиенко , ur .
7 kwietnia 1949 w Szepetówce ) – rosyjska polityk , w latach 2003–2011 gubernator Petersburga , przewodnicząca Rady Federacji .
Życiorys .
Ukończyła Instytut Chemiczno-Farmaceutyczny w Leningradzie i w 1985 Akademię Nauk Społecznych KPZR .
W 1972 rozpoczęła karierę polityczną w Komunistycznej Partii Związku Radzieckiego .
W latach 90 .
XX wieku sprawowała urząd ambasadora ZSRR ( a potem Rosji ) w Grecji i na Malcie .
Działała również w Ministerstwie Spraw Zagranicznych .
Przed objęciem stanowiska gubernatora Petersburga , w latach 1998−2003 , była wicepremierem Rosji w rządach czterech kolejnych premierów .
Od 21 września 2011 pełniła funkcję przewodniczącej Rady Federacji .
17 marca 2014 została objęta zakazem wjazdu do Stanów Zjednoczonych i krajów Unii Europejskiej , w ramach retorsji za agresję Rosji przeciw Ukrainie – uznano , że była jednym z prowodyrów rosyjskiej histerii antyukraińskiej ( wielokrotnie opowiadała publicznie , także w czasie debaty w rosyjskim parlamencie , o represjach ukraińskich i ofiarach wśród rosyjskojęzycznej mniejszości na Ukrainie , co nie miało pokrycia w faktach ) .
