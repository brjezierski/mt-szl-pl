Walenty Fojkis
Włodzimierz Walenty Fojkis ( ur .
13 lutego 1895 w Katowicach , zm .
26 października 1950 w Edynburgu ) – polski działacz narodowy na Górnym Śląsku .
Życiorys .
Urodził się w Wełnowcu .
Przed I wojną światową był członkiem Towarzystwa Gimnastycznego „ Sokół ” .
Podczas I wojny został wcielony do armii niemieckiej i został ranny pod Lens .
Po skierowaniu na leczenie do Wielunia nawiązał kontakt z POW .
Od 13 czerwca do 27 września 1919 był uczniem 14. klasy Szkoły Podchorążych w Warszawie .
Po ukończeniu nauki został przydzielony do 17. pułku piechoty .
Od lutego 1920 roku przebywał na urlopie na Górnym Śląsku w celu wzięcia udziału w akcji plebiscytowej .
Otrzymał funkcję dowódcy V ( bytomsko-tarnogórskiego ) okręgu POWGŚl ( Polska Organizacja Wojskowa Górnego Śląska ) , a po reorganizacji w czerwcu 1920 dowódcy VII ( pszczyńsko-katowickiego ) okręgu POWGŚl .
Był pierwszym , który ruszył ze swoim oddziałem do II powstania śląskiego .
Od grudnia 1920 do kwietnia 1921 był komendantem POW Górnego Śląska w powiecie katowickim .
W III powstaniu śląskim był dowódcą 1. pułku katowickiego .
Nosił wtedy pseudonim „ Stawski ” .
8 stycznia 1924 został zatwierdzony w stopniu porucznika ze starszeństwem z 1. czerwca 1919 i 3835. lokatą w korpusie oficerów rezerwy piechoty .
Posiadał wówczas przydział w rezerwie do 72 .Pułk Piechoty w Radomiu .
W okresie międzywojennym był naczelnikiem gminy Michałkowice .
Był członkiem Chrześcijańskiej Demokracji i posłem do Sejmu RP i Sejmu Śląskiego .
Działał też w Związku Powstańców Śląskich .
W 1934 , będąc oficerem rezerwy , figurował w ewidencji Powiatowej Komendy Uzupełnień Katowice .
Posiadał przydział w rezerwie do 12 .
Pułku Piechoty w Wadowicach .
Organizował Ochotnicze Oddziały Powstańcze .
Po kampanii 1939 roku pełnił służbę w Polskich Siłach Zbrojnych na Zachodzie .
29 czerwca 1950 otrzymał brytyjskie obywatelstwo .
Zmarł w Edynburgu w Wielkiej Brytanii w 1950 roku .
Upamiętnienie .
Jego imię nosi plac na rodzinnym Wełnowcu , ulica w Katowicach–Murckach , w Rudzie Śląskiej i w innych miastach .
Jego postać została uwidoczniona ( pośród innych ) na muralu w Siemianowicach Ślaskich .
