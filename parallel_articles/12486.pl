Walenty Wojciech
Walenty Wojciech ( ur .
2 lutego 1868 w Grzawie , zm .
29 maja 1940 we Wrocławiu ) – biskup tytularny Danaba , biskup pomocniczy wrocławski w latach 1920–1940 .
Urodził się 2 lutego 1868 roku w Grzawie koło Pszczyny w rodzinie wolnego sołtysa .
Ukończył gimnazjum w Pszczynie oraz studia teologiczne na uniwersytecie we Wrocławiu .
Po uzyskaniu święceń kapłańskich w roku 1894 pracował jako wikariusz w parafiach w Katowicach i Kluczborku .
Przez krótki czas był proboszczem parafii w Lewinie Brzeskim , a następnie w Mieroszowie .
W 1902 przeniesiono go do Korfantowa , gdzie sprawował funkcję proboszcza do 1916 roku .
W tym czasie z jego inicjatywy wybudowano w 1904 roku nowy kościół pod wezwaniem św .
Apostołów Piotra i Pawła w Rzymkowicach oraz w latach 1908–1911 w Korfantowie na miejscu rozebranej starej świątyni wzniesiono nowy kościół pod wezwaniem Trójcy Świętej .
Zadbał również o remont kaplic w miejscu pielgrzymek na Szwedzkiej Górce koło Przydroża Małego .
W roku 1916 roku został kanonikiem wrocławskim i radcą kurii biskupiej , parafię w Korfantowie natomiast objął po nim , będący również księdzem , jego brat Antoni Wojciech .
Sakrę biskupią otrzymał 1 maja 1920 roku .
Działalność duszpasterską prowadził w języku polskim i niemieckim .
