Walia
Walia ( wal .
`` Cymru '' , wym .
; ang .
`` Wales '' , wym . )
– kraj stanowiący część Zjednoczonego Królestwa Wielkiej Brytanii i Irlandii Północnej .
Także celtycka kraina historyczna .
Kraj jest położony w południowo-zachodniej części wyspy Wielka Brytania , na zachód od Anglii , nad Morzem Irlandzkim i Celtyckim .
Stolicą Walii jest Cardiff .
Mimo że kraj ten dzieli historię społeczno-polityczną z resztą Wielkiej Brytanii , a zdecydowana większość ludności mówi po angielsku , Walia zachowała odrębność kulturową i jest krajem dwujęzycznym .
Ponad 560 tys .
mieszkańców posługuje się językiem walijskim , który szczególnie rozpowszechniony jest w zachodnich i północnych częściach kraju .
Od końca XIX wieku Walia zyskała wizerunek „ kraju pieśni ” , po części dzięki tradycji `` eisteddfod '' .
Kraj reprezentowany jest przez własną drużynę na wielu międzynarodowych imprezach sportowych , m.in .
w Pucharze Świata FIFA , Mistrzostwach Europy UEFA , Pucharze Świata w Rugby oraz na Igrzyskach Wspólnoty Narodów .
Rugby union uważane jest za symbol walijskiej tożsamości narodowej oraz przejaw narodowej świadomości .
Pochodzenie nazwy .
Nazwa Walia jest spolszczoną wersją angielskiej nazwy Wales , która jest germańskim egzonimem pochodzącym od germańskiego słowa `` Walha '' i oznacza nieznajomego lub cudzoziemca .
Słowo `` Walha '' prawdopodobnie wywodzi się od nazwy celtyckiego plemienia `` Volcae '' .
Walijczycy z kolei nazywają swój kraj Cymru ( wym .
) , a siebie Cymry , co w języku starowalijskim znaczy „ rodak ” , „ krajan ” .
Historia .
Od prehistorii do starożytności .
Walia od przynajmniej 29 tys .
lat jest zamieszkiwana przez ludzi współczesnych .
Za początki stałego zamieszkania uznaje się końcówkę epoki lodowcowej między 12 a 10 tys .
lat temu .
Myśliwi z ery mezolitycznej , zamieszkujący tereny centralnej Europy , rozpoczęli migrację na tereny Wielkiej Brytanii .
W tamtym czasie poziom morza był znacznie niższy niż obecnie , co więcej , płytsze warstwy obecnego Morza Północnego były kiedyś suchym lądem .
Obecne wschodnie wybrzeże Anglii oraz wybrzeża Danii , Niemiec oraz Holandii połączone były obszarem lądu znanym jako Doggerland ( obszar zamieszkały przez paleolityczne ludy koczownicze ) , tworząc w ten sposób Półwysep Brytyjski na kontynencie Europejskim .
Walia nie posiadała lodowców do około 10 250 lat temu , a cieplejszy klimat przyczynił się do gęstego zalesienia w tej części kontynentu .
Wzrost poziomu morza po okresie lodowcowym oddzielił Walię i Irlandię , kształtując Morze Irlandzkie .
Doggerland został zatopiony przez Morze Północne i 8000 lat temu Półwysep Brytyjski przekształcił się w wyspę .
