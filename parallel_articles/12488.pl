Historia .
Starowalijski .
Najstarsze ślady języka identyfikowanego z walijskim sięgają VI stulecia , język ten nazywany jest `` wczesnowalijskim '' ; zachowało się niewiele jego przykładów .
Następny ważny okres to starowalijski ( Hen Gymraeg , IX do XI stulecia ) , nieco lepiej poświadczony .
Jego przykłady zachowały się w postaci poezji walijskiej i szkockiej .
Średniowalijski .
Średniowalijski to nazwa nadana formie języka walijskiego używanego pomiędzy XII a XIV stuleciem , której zachowanych przykładów istnieje znacznie więcej od poprzednich .
Język ten był bliski jednemu z najstarszych manuskryptów Mabinogionu , chociaż samo dzieło musi być znacznie starsze .
Jest w pewnym stopniu zrozumiały dla współczesnego Walijczyka .
Współczesny walijski .
Wczesny współczesny walijski .
Współczesny język walijski można podzielić na dwa okresy .
Pierwszy , wczesny współczesny walijski , trwał od początku XV do końca XVI stulecia i był używany przez Dafydd ap Gwilym .
Późny współczesny walijski .
Późny współczesny walijski datuje się od publikacji przekładu Biblii , dokonanego przez biskupa Williama Morgana w roku 1588 .
Podobnie jak jego angielski odpowiednik , Biblia króla Jakuba , spowodował on ustabilizowanie się języka – i rzeczywiście współcześnie brzmi on prawie tak samo jak u Morgana , choć oczywiście zaszły pewne drobne zmiany .
XIX wiek .
Język cieszył się wtedy wzrastającym znaczeniem wraz z publikacją pierwszych walijskich słowników .
Praca leksykografów , takich jak pastor Daniel Silvan Evans , spowodowała udokumentowanie języka tak dokładnie jak to możliwe .
Współczesne słowniki , jak Geiriadur Prifysgol Cymru ( Słownik Uniwersytetu Walijskiego ) kierują się właśnie tymi pierwszymi .
Wpływy języka angielskiego w czasie Rewolucji Przemysłowej od około 1800 roku doprowadziły do zmniejszenia się liczby mówiących walijskim .
Przybysze z Anglii rzadko uczyli się walijskiego , a Walijczycy w relacjach z nimi z konieczności używali angielskiego .
Status prawny walijskiego zmniejszył się w porównaniu z angielskim , który stopniowo zaczął dominować , z wyjątkiem terenów wiejskich , szczególnie w północno-zachodniej i środkowej Walii .
Wyjątek stanowiły tradycjonalne kościoły , mocno przywiązane do języka walijskiego .
XX i XXI wiek .
W XX stuleciu liczba użytkowników walijskiego zaczęła gwałtownie się kurczyć i twierdzono , że wygaśnie on w przeciągu kilku pokoleń .
Spis powszechny zaczął zadawać pytania o język w 1891 roku , w tym czasie 54 % ludności wciąż posługiwało się walijskim .
Liczba ta malała z każdym kolejnym spisem , osiągając najniższy wynik w 1981 ( 19 % ) , który utrzymał się w 1991 , natomiast w roku 2001 wyniósł 21 % .
W spisie z roku 2001 odnotowano także , iż 20 % osób potrafiło po walijsku czytać , 18 % pisać , a 24 % rozumieć mówiony walijski .
Co więcej , największy ich odsetek był wśród młodych ludzi , co dobrze rokuje temu językowi .
W roku 2001 , 39 % dzieci w wieku 10-15 lat potrafiło mówić , czytać i pisać po walijsku ( wiele z nich poznało go w szkołach ) , odpowiednio 25 % w wieku 16-19 lat .
Jednakże , odsetek mówiących walijskim na obszarach , gdzie język ten jest głównym , wciąż maleje .
Wydaje się , że to wzrastający walijski nacjonalizm utrzymuje ten język przy życiu , a powstanie walijskiego radia i telewizji zachęciło do jego poznawania .
Wprowadzenie Internetu dało okazje do posługiwania się walijskim w formie pisemnej .
Prawdopodobnie najważniejsze okazało się wprowadzenie obowiązku nauczania walijskiego w szkołach dzieci do 16 lat , co spowodowało zwiększenie się popularności języka na obszarach walijskojęzycznych i podstawowej jego znajomości tam , gdzie dominuje angielski .
Spadek liczby Walijczyków znających walijski został zahamowany , a nawet obserwuje się wzrost jego popularności .
Obecnie walijski stał się językiem codziennym w wielu regionach Walii .
