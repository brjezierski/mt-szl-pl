Ortografia .
Język walijski zapisywany jest wersją alfabetu łacińskiego tradycyjnie zawierającą 29 liter , z których osiem jest dwuznakami traktowanymi jak jedna litera :
a , b , c , ch , d , dd , e , f , ff , g , ng , h , i , j , l , ll , m , n , o , p , ph , r , rh , s , t , th , u , w , y
Litera „ j ” jest powszechnie włączana do alfabetu pomiędzy „ i ” a „ l ” , ponieważ stosuje się ją w niektórych wyrazach zapożyczonych z angielskiego ( np .
nazwisko Jones ) .
Litery „ k ” , „ q ” , „ v ” , „ x ” i „ z ” używane są w niektórych terminach technicznych , jak `` kilogram '' , `` volt '' , `` xeroser '' czy `` zero '' , lecz mogą zostać zastąpione ( co często się robi ) literami walijskimi : `` cilogram '' , `` folt '' , `` seroser '' i `` sero '' .
Najczęstszym znakiem diakrytycznym jest cyrkumfleks , używany do zaznaczenia długich samogłosek .
