W Antibes mieszkał wybitny pisarz grecki Nikos Kazandzakis , który osiadł tu w 1948 r. To właśnie tu powstały jego najsłynniejsze utwory , w tym powieści `` Grek Zorba '' i `` Ostatnie kuszenie Chrystusa '' .
