Wallgau
Wallgau – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , w powiecie Garmisch-Partenkirchen .
Leży około 15 km na wschód od Garmisch-Partenkirchen , nad Izarą , przy drodze B11 .
Polityka .
Wójtem gminy jest Hansjörg Zahler , rada gminy składa się z 12 osób .
Oświata .
W gminie znajduje się przedszkole ( 50 miejsc ) oraz szkoła podstawowa połączona z częścią Hauptschule ( 10 nauczycieli , 154 uczniów ) .
