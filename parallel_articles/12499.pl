Walt Disney
Walter Elias Disney ( ur .
5 grudnia 1901 w Chicago , zm .
15 grudnia 1966 w Los Angeles ) – amerykański animator , aktor głosowy , producent filmowy , przedsiębiorca , wizjoner i filantrop .
Jeden z największych twórców przemysłu rozrywkowego w historii , legenda animacji i filmu .
Zdobył rekordową liczbę 59 nominacji do Oscarów , z czego zdobył również rekordową liczbę 26 statuetek , siedmiokrotny zdobywca Nagrody Emmy .
Wraz z bratem , Royem Oliverem Disneyem , założyli wspólnie przedsiębiorstwo Walt Disney Productions , znaną obecnie pod nazwą The Walt Disney Company , drugą co do wielkości korporację mediową świata .
Jest twórcą takich postaci jak Myszka Miki ( pod którą Disney początkowo sam podkładał głos ) , pies Pluto czy Kaczor Donald .
W 1955 założył pierwszy park rozrywki Disneyland .
