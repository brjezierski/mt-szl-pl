Antichrist Superstar
Antichrist Superstar – drugi album studyjny grupy Marilyn Manson , wydany 8 października 1996 roku .
Album odniósł komercyjny sukces i rozsławił grupę , ale nie ominęły go kontrowersje : chrześcijańska grupa `` The American Family Association '' oskarżyła grupę o m.in .
zbyt agresywne występy i antychrześcijańskie utwory .
`` Antichrist Superstar '' jest pierwszym albumem trylogii , do której należą odpowiednio : Mechanical Animals i Holy Wood ( In the Shadow of the Valley of Death ) .
Album sprzedał się w nakładzie 7 milionów kopii , w tym 1.9 milionów w samych Stanach Zjednoczonych .
Wydano pięć singli : The Beautiful People i Tourniquet , Cryptorchid , Antichrist Superstar i Man That You Fear .
Lista utworów .
Album jest podzielony na trzy części , nazwane `` cycles '' .
Cycle I – The Heirophant
Cycle II – Inauguration of the Worm
Cycle III – Disintegrator Rising
