Waltershausen
Waltershausen – miasto w Niemczech , w kraju związkowym Turyngia , w powiecie Gotha .
W 1984 roku miasto miało 14 288 mieszkańców , a w 2014 roku liczyło już 13 038 mieszkańców .
Do 3 października 1990 roku miasto leżało w granicach NRD .
W mieście znajduje się fabryka mikrosamochodów ciężarowych i komunalnych Multicar .
31 grudnia 2013 roku do miasta przyłączono gminę Emsetal , która stała się automatycznie jego dzielnicą .
