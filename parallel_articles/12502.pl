Walendów
Walendów – wieś w Polsce położona w województwie mazowieckim , w powiecie pruszkowskim , w gminie Nadarzyn .
Leży nad rzeką Utratą .
Wieś szlachecka Walentowo położona była w drugiej połowie XVI wieku w powiecie błońskim ziemi warszawskiej województwa mazowieckiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa warszawskiego .
Na terenie wsi znajduje się kilkanaście zbiorników wodnych ( m.in .
Staw Oborowy , Staw Młyński ) .
W Walendowie siostry ze Zgromadzenia Sióstr Matki Bożej Miłosierdzia posiadają od roku 1913 swoją placówkę zakonną , w której obecnie prowadzą dom dziecka .
W klasztorze tym w listopadzie 1932 przebywała św .
Faustyna Kowalska , odbywając ośmiodniowe rekolekcje przed rozpoczęciem trzeciej probacji .
Do tego samego domu zakonnego trafiła ponownie w marcu 1936 .
