1 lipca 1952 roku jednostka o nazwie `` gmina Wancerzów '' została zniesiona przez przemianowanie na `` gminę Mstów '' ; dokonano wtedy też wymiany części terenów z sąsiednią gminą Rędziny .
W dniu powołania gmina Mstów była podzielona na 13 gromad : Brzyszów , Cegielnia , Gąszczyk , Jaskrów , Kłobukowice , Kuchary , Małusy , Małusy Wielkie , Mstów , Siedlec I , Srocko , Wancerzów i Zawada .
