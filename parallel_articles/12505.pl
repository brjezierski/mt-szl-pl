Siedzibą władz gminy był Wancerzów .
Gmina Wancerzów powstała za Królestwa Polskiego w 1868 roku z obszaru zniesionej gminy Krasice .
Jednostka należała do powiatu częstochowskiego w guberni piotrkowskiej .
do gminy przyłączono pozbawiony praw miejskich Mstów .
W okresie międzywojennym gmina Wancerzów należała do powiatu częstochowskiego w woj .
