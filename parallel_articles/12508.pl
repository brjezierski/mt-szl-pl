Wangaratta
Wangaratta – miasto w Australii w stanie Wiktoria , położone nad rzeką Ovens , 230 km od Melbourne .
Jest stolicą diecezji , zamieszkaną przez ponad 17 000 ludzi i najbardziej zaludnionym miastem w tym obszarze samorządu lokalnego .
Jest położona około 230 km od Melbourne , 45 km na południowy zachód od miasta Benalla i 72 km na północny wschód od miasta Albury-Woodonga , wzdłuż autostrady Hume Highway , nad skrzyżowaniem rzeki Ovens z King , które mają swe źródło w Wielkich Górach Wododziałowych .
