Herb Katowic
Herb Katowic – jeden z symboli miejskich Katowic .
Wygląd i symbolika .
Herb przedstawia na złotej tarczy czarne urządzenie kuźnicze umocowane na drewnianym stelażu symbolizowanym przez brązową belkę drewnianą , pod nią zlokalizowane jest błękitnej barwy pole symbolizujące wodę napędzającą urządzenie kuźnicze .
Młot mechaniczny składa się : z kowadła , młota kuźniczego napędzanego kołem zębatym , współosiowo połączonym wałem z kołem wodnym .
Blazon : `` W polu złotym czarny młot wodny z kowadłem zwrócony w prawo , z kołem zębatym po lewej stronie tarczy ; młot stoi na belce brunatnej nad błękitną wodą . ''
Historia .
Po raz pierwszy herb pojawił się na pieczęci gminnej w 1816 − przedstawiał młot mechaniczny .
Z jednej strony młot spoczywał na kowadle , z drugiej strony był napęd młota − koło wodne .
Nazwa osady brzmiała `` Bogutzerhammer '' , gdyż w tym okresie wraz z nazwą Katowice często pojawiała się nazwa Kuźnica Bogucka ( nazwa pierwotna pierwszej osady w miejscu Śródmieścia Katowic ) .
Symbolika związana jest z miejscem pracy wielu mieszkańców wsi Katowice ( Kuźnica Bogucka ) .
Na pieczęciach gminnych z lat 30 .
XIX wieku widnieje już nazwa `` KATTOWITZ '' oraz informacja , że miejscowość należy do powiatu bytomskiego ( `` BEUTHEN KREIS '' ) .
Wygląd herbu nie uległ zmianie .
Po nadaniu praw miejskich w 1865 r. przez Króla Prus Wilhelma I nie nastąpiło oficjalne nadanie herbu , co było raczej rzadkie wśród nowych miast Górnego Śląska .
