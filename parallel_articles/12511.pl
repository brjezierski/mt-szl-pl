Herb Niemiec
Herb Niemiec – jeden z obowiązujących symboli Niemiec , obok m.in .
flagi i hymnu .
Kolorystyka państwowego herbu niemieckiego odpowiada fladze Republiki Federalnej Niemiec .
Ustawa Zasadnicza Republiki Federalnej Niemiec nie wspomina o herbie .
Stosowanie w tym charakterze wizerunku orła , mające długą tradycję , zostało usankcjonowane obwieszczeniem prezydenta z 20 stycznia 1950 .
Wygląd .
Herb przedstawia czarnego orła z głową odwróconą w prawo ( heraldycznie ) , czerwonym dziobem i szponami tego koloru , na złotej tarczy .
Skrzydła orła uniesione są do wysokości głowy , jednocześnie mając skierowane w dół lotki .
Historia .
Orzeł jako godło historią sięga starożytności , kiedy był symbolem cesarzy rzymskich .
Również pierwszy frankijski średniowieczny cesarz rzymski , Karol Wielki , gromadzący w swym rozległym imperium narody germańskie , na szczycie pałacu w Akwizgranie – według relacji Richera z Reims i Thietmara z Merseburga – umieścił rzeźbę orła .
