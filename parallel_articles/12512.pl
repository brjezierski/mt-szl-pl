Herb Nowego Sącza
Herb Nowego Sącza – jeden z symboli miejskich Nowego Sącza .
Wygląd i symbolika .
Herb przedstawia w błękitnym polu postać świętej Małgorzaty , stojącej na grzbiecie zielonego smoka .
Święta Małgorzata ubrana jest w białą suknię ze złotym paskiem , a na ramiona ma zarzuconą czerwoną pelerynę , spiętą na piersiach złotą spinką .
Jej głowę zdobi złota korona i nimb .
W lewej , lekko uniesionej ręce , trzyma złoty krzyż łaciński na długim drzewcu , którym godzi w rozwartą paszczę smoka .
W prawej ręce trzyma złotą gałązkę palmową .
Smok – na którym stoi – przedstawiony z boku , jest wygięty na kształt półksiężyca .
Jego paszcza i zakręcony ogon są uniesione ku górze .
W połowie długości smoka widoczna jest szponiasta łapa i smocze skrzydło .
Postać patronki miasta umieszczonej na smoku nawiązuje do legendy o świętej Małgorzacie , która ujarzmiła smoka piekielnego .
Gałązka palmowa jest symbolem męczeństwa – święta Małgorzata była prześladowana za wiarę i ścięta w Antiochii na przełomie III i IV wieku .
Historia .
Postać świętej Małgorzaty stojącej na smoku występowała w herbie od momentu założenia miasta w 1292 r. Zgodnie ze średniowiecznym zwyczajem na herb miasta wybrano świętą , patronującą miejscowemu kościołowi farnemu .
