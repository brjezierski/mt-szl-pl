Herb Rzymu
Herb Rzymu ( wł .
: `` Stemma della città di Roma '' ) – herb , symbol miasta Rzym we Włoszech .
Opis herbu .
Składa się z czerwonej średniowiecznej tarczy francuskiej , w polu której umieszczony złoty krzyż grecki oraz przechylony na lewo w dół skrót SPQR oznaczający łacińską formułę `` Senatus Populusque Romanus '' , która w tłumaczeniu oznacza `` senat i lud rzymski '' .
Nad tarczą umieszczona jest złota , otwarta korona rangowa .
