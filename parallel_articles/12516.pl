Herb Sztokholmu
Herb Sztokholmu przedstawia w błękitnej tarczy herbowej złotą koronowaną głowę .
Jest to wyobrażenie Świętego Eryka , patrona miasta .
Został on wybrany królem Szwecji w 1155 .
Według podania , w miejscu jego śmierci , pod północną ścianą katedry w Uppsali , cudownym sposobem wytrysnęło źródło , bijące do dzisiaj .
Uważano , że wypływająca z niego woda ma właściwości lecznicze .
Ponieważ król cieszył się opinią dobrego chrześcijanina oraz sprawiedliwego władcy , a także w wyniku uznania utworzenia źródła za cudowne , mieszczanie sztokholmscy wybrali go patronem miasta .
Zadaniem króla-patrona jest ochrona miasta .
Eryk IX nigdy nie został uznany za świętego przez Kościół , jednakże istnieje w Szwecji jego lokalny kult , a kilka szwedzkich kościołów jest pod jego wezwaniem .
Wyobrażenie w godle herbowym jest identyczne z zachowanymi na średniowiecznych pieczęciach miejskich .
Oficjalnie przyjęty został 19 stycznia 1934 roku .
