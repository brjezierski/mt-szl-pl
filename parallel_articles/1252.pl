Antigua i Barbuda
Antigua i Barbuda ( ) – wyspiarskie państwo na Morzu Karaibskim , w archipelagu Małych Antyli ( Ameryka Środkowa ) .
Głową państwa jest król Wielkiej Brytanii , reprezentowany na wyspach przez gubernatora generalnego .
Podstawą gospodarki jest turystyka .
Do lat 70 .
XX wieku w gospodarce dominowało rolnictwo przemysłowe .
Podział administracyjny jest nietypowy .
Antigua , zamieszkana przez 98 % ludności , jest podzielona na 6 okręgów , natomiast pozostałe wyspy są dependencjami .
Symbole państwowe .
Flaga .
Flaga została zaprojektowana przez Reginalda Samuela w 1966 roku .
Siedmiopromieniowe złote słońce symbolizuje nową erę .
