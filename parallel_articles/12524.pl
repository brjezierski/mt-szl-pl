Warka ( piwo )
Warka – jasne piwo dolnej fermentacji typu lager o zawartości ekstraktu 12,5 % oraz alkoholu 5,2 % .
Produkowane jest w browarze w Warce od 1975 r. oraz w browarze w Żywcu i browarze w Elblągu .
Nazwa i historia piwa .
Nazwa piwa wywodzi się zarówno od miasta , w którym powstało , jak i od określenia piwowarskiej miary , oznaczającej porcję piwa uzyskanego z jednego warzenia .
W przeszłości piwo Warka warzone było w różnych stylach i o zróżnicowanej zawartości ekstraktu i alkoholu .
