Województwo warmińsko-mazurskie
Województwo warmińsko-mazurskie – jednostka podziału administracyjnego Polski , jedno z 16 województw .
Położone jest w północno-wschodniej części kraju .
Siedzibą wojewody i władz samorządu województwa jest Olsztyn .
Obejmuje obszar 24 173,47 km² ( stan na 31 grudnia 2017 ) i liczy około 1,42 mln mieszkańców ( 30 czerwca 2020 ) .
Historia .
Województwo warmińsko-mazurskie powstało w 1999 r. w wyniku reformy podziału administracyjnego kraju i objęło tereny dawnego województwa : olsztyńskiego , większe części województw elbląskiego i suwalskiego oraz fragmenty toruńskiego , ciechanowskiego i ostrołęckiego .
Geografia .
Według danych z 2 stycznia 2014 r. powierzchnia województwa wynosiła 24 173,47 km² , co stanowi 7,7 % powierzchni Polski .
Jest czwarte pod względem powierzchni spośród województw .
