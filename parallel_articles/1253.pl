Antoine Griezmann
Antoine Griezmann ( ur .
21 marca 1991 w Mâcon ) – francuski piłkarz pochodzenia niemiecko-portugalskiego , występujący na pozycji napastnika w hiszpańskim klubie Atletico Madryt oraz w reprezentacji Francji .
Od 2014 reprezentant Francji w piłce nożnej .
Złoty medalista Mistrzostw Europy U-19 2010 , Mistrzostw Świata 2018 i Ligi Narodów UEFA 2020/2021 , srebrny medalista Mistrzostw Europy 2016 i Mistrzostw Świata 2022 .
Uczestnik Mistrzostw Świata 2014 , 2018 i 2022 .
Kariera klubowa .
Początki .
Karierę piłkarską rozpoczął w rodzinnym mieście Mâcon .
Trenował w tamtejszych klubach EC Mâcon i UF Mâcon .
W 2005 podczas testów w Montpellier HSC wzbudził zainteresowanie Realu Sociedad i następnie przeniósł się do tego klubu , mającego siedzibę w San Sebastián .
W latach 2005–2009 grał w młodzieżowych zespołach Realu .
Real Sociedad .
Latem 2009 Griezmann awansował do kadry pierwszej drużyny Realu Sociedad , prowadzonej przez Urugwajczyka Martína Lasarte .
2 września 2009 zaliczył debiut w pierwszym zespole Realu Sociedad w meczu Pucharu Króla z Rayo Vallecano ( 0:2 ) .
