Warrnambool
Warrnambool – miasto w stanie Wiktoria w Australii .
W mieście kończy się popularna droga turystyczna Great Ocean Road .
Warrnambool położone jest 265 km ( 3 godziny samochodem lub pociągiem ) na zachód od Melbourne , jadąc drogą Princes Highway .
W okresie zimowym ( lipiec , sierpień ) w okolicy plaży Logan 's Beach można oglądać wieloryby ( wale biskajskie południowe ) , które przypływają co roku , aby w tym miejscu urodzić potomstwo .
W skład miasta wchodzi wyspa Middle Island - wyspa pingwinów .
