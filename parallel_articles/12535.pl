Warta ( wojsko )
Warta – uzbrojony pododdział , wyznaczony rozkazem dziennym , do
pełnienia służby wartowniczej , polegającej na ochronie wyznaczonych obiektów , rejonów lub osób .
Warta składa się z dowódcy , jego pomocnika , rozprowadzających i wartowników .
Według uregulowań przyjętych w Wojsku Polskim służbę wartowniczą mogą pełnić żołnierze , którzy złożyli przysięgę wojskową , zdali egzamin z prawa użycia broni oraz przeszli badania lekarskie i instruktaż .
