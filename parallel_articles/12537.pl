Warcino
Warcino ( kaszb .
`` Warcëno '' , niem .
`` Varzin '' ) – wieś w Polsce położona w województwie pomorskim , w powiecie słupskim , w gminie Kępice przy drodze wojewódzkiej nr 208 .
W kierunku północno-zachodnim , przy granicy z województwem zachodniopomorskim znajduje się jezioro Lewarowe .
Historia .
W latach 1945–1954 siedziba gminy Warcino .
W latach 1975–1998 miejscowość leżała w województwie słupskim .
W latach 1867–1945 dobra warcińskie należały do rodziny Bismarcków .
Kanclerz Otto von Bismarck nabył XVII-wieczny dwór w 1867 roku .
16 września 1894 przybyła tu do Bismarcka delegacja Niemców z Poznańskiego , a kanclerz wygłosił do niej przemówienie , w którym wskazał polską szlachtę i duchowieństwo jako głównych antyrządowych przywódców ludu polskiego , podżegających do wystąpień przeciw Prusakom .
Sam lud polski traktował natomiast jako element bierny i lojalny wobec zaborcy .
Zachował się zespół pałacowy i zespół dworski położone w parku krajobrazowym o pięknym drzewostanie m.in .
100 drzew pomnikowych .
Pałac powstał w XVII wieku i był wielokrotnie rozbudowywany , najstarsze jest późnorenesansowe skrzydło z 1664 , które pierwotnie było dworem myśliwskim .
