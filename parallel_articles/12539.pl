Warnino ( powiat koszaliński )
Warnino ( niem .
`` Warnin '' ) – wieś w Polsce położona w województwie zachodniopomorskim , w powiecie koszalińskim , w gminie Biesiekierz .
Według danych z 31 grudnia 2005 r. wieś miała 160 mieszkańców .
Warnino od XIV wieku do 1658 roku należało do rodu rycerskiego von Parsow .
W XVIII wieku folwark oraz wieś były częściowo własnością rotmistrza Georga Christofa von Kameke .
