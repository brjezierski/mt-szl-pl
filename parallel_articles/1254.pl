Antoni Piechniczek
Antoni Krzysztof Piechniczek ( ur .
3 maja 1942 w Chorzowie ) – polski piłkarz , trener piłkarski , szkoleniowiec reprezentacji Polski , polityk , senator VII kadencji , od 2008 do 2012 wiceprezes zarządu PZPN .
Życiorys .
Rodzina i kariera zawodnicza .
Syn Emila Piechniczka i Magdaleny z domu Marczok .
Jego ojciec pochodził z Raciborza , pod koniec II wojny światowej jako Ślązak został przymusowo wcielony do Wehrmachtu , później dostał się do niewoli radzieckiej , z której nie wrócił .
Uznany za zmarłego 9 maja 1945 w sowieckim łagrze w Witebsku .
Antoni Piechniczek karierę piłkarską zaczynał w Zrywie Chorzów , następnie grał w Naprzodzie Lipiny ( 1960–1961 ) , Legii Warszawa ( 1961–1965 ) , Ruchu Chorzów ( 1965–1972 ) i francuskim Chateauroux ( 1972–1973 ) .
W 1964 zdobył z Legią Puchar Polski , a w 1968 mistrzostwo Polski z Ruchem .
Rozegrał trzy mecze w reprezentacji Polski ( 1967–1969 ) .
Grał w pomocy i na prawej obronie .
Trener i selekcjoner .
Ukończył studia w Akademii Wychowania Fizycznego w Warszawie .
W 1972 był kandydatem na szkoleniowca Stali Stalowa Wola , nie doszedł jednak do porozumienia z klubem po tym , jak zaproponowano mu poprowadzenie zespołu wspólnie z Jerzym Kopą .
Jako trener pracował natomiast w BKS Bielsko-Biała , Odrze Opole , Ruchu Chorzów i Górniku Zabrze , a także w klubach zagranicznych z Kataru i Zjednoczonych Emiratów Arabskich .
Prowadził też tunezyjską drużynę Espérance Tunis .
W 1987 zdobył mistrzostwo Polski z Górnikiem Zabrze .
Pod koniec grudnia 1980 został powołany na stanowisko trenera-selekcjonera reprezentacji Polski .
Oficjalnie objął obowiązki 5 stycznia 1981 .
Uzyskał awans na mistrzostwa świata w Hiszpanii w 1982 , na których polska drużyna zajęła trzecie miejsce .
Wywalczył również awans na mistrzostwa świata w Meksyku w 1986 , gdzie reprezentacja Polski przegrała w 1/8 finału .
Złożył rezygnację w czerwcu 1986 .
Ponownie objął funkcję selekcjonera reprezentacji w maju 1996 .
Nie udało mu się jednak doprowadzić do awansu na mistrzostwa świata we Francji .
Podał się do dymisji 7 czerwca 1997 .
Pracował również jako selekcjoner reprezentacji Tunezji ( udział w Letnich Igrzyskach Olimpijskich w Seulu w 1988 ) oraz Zjednoczonych Emiratów Arabskich .
Działalność zawodowa i polityczna .
Długoletni działacz Polskiego Związku Piłki Nożnej .
W latach 2008–2012 pełnił funkcję wiceprezesa PZPN .
Zasiadał również w radzie rady nadzorczej Odry Wodzisław Śląski .
Został wykładowcą Akademii Wychowania Fizycznego w Katowicach .
Wielokrotnie zapraszany do studia telewizyjnego w charakterze komentatora i eksperta .
W 2002 został wybrany z listy Unii Samorządowej ( związanej z Unią Wolności ) do sejmiku śląskiego .
Pełnił funkcję wiceprzewodniczącego sejmiku .
Otwierał listę kandydatów Partii Demokratycznej – demokraci.pl do Sejmu w okręgu katowickim w wyborach w 2005 .
W grudniu tego samego roku przystąpił w sejmiku do klubu radnych Platformy Obywatelskiej .
Z jej listy w 2006 ponownie został radnym województwa .
W wyborach parlamentarnych w 2007 z ramienia PO uzyskał mandat senatorski w okręgu katowickim , otrzymując 207 243 głosy .
W 2011 nie ubiegał się o reelekcję .
W eurowyborach w 2014 poparł Polskę Razem .
