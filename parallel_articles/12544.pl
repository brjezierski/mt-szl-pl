We wczesnym dzieciństwie stracił ojca i był wychowywany przez Daniiła Ochłopkowa , pisarza ułusu , z którego pochodził Nikiforow .
Ukończył progimnazjum w Jakucku , a następnie w trybie zaocznym ukończył studia prawnicze .
Kształcił się również pod kierunkiem zesłańców politycznych , m.in .
narodnik i lekarz A. Sipowicz uczył go zawodu felczera .
Od początku lat 90 .
XX wieku był starostą swojego ułusu .
Na tym stanowisku prowadził działalność kulturalną i oświatową , organizując budowę nowych obiektów według rosyjskich wzorców , a w 1893 otwierając , za własne fundusze , szkołę cerkiewno-parafialną , walczył z pijaństwem i grą w karty .
W latach 1894-1896 wziął udział w zorganizowanej przez Rosyjską Akademię Nauk ekspedycji , której celem było badanie kultury ludowej i sposobu życia rdzennych ludów północno-wschodniej Azji .
Przy użyciu zgromadzonego materiału napisał pracę poświęconą życiu rodzinnemu Jakutów ( zaginęła ) .
W 1896 organizował udział Jakutów w pierwszej w Rosji wystawie artystyczno-przemysłowej w Niżnym Nowogrodzie .
Od tego samego roku był zatrudniony w komitecie statystycznym w Jakucku .
Wspólnie z zesłańcem E. Piekarskim opracowywał słownik języka jakuckiego , przekładał na język rosyjski ludowe pieśni jakuckie .
W 1897 był wśród organizatorów biblioteki naukowej w Jakucku .
Pod wpływem wydarzeń rewolucji r. 1905 założył Związek Jakutów , który został uznany przez władze carskie za organizację wywrotową .
Dziesięciu członków kierownictwa organizacji zostało aresztowanych ; Nikiforow przebywał w więzieniu przez półtora roku .
