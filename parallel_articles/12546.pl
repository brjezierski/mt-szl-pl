Wasiłowice
Wasiłowice ( dodatkowa nazwa w j. niem .
Waschelwitz ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Biała .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Kotliny Raciborskiej , będącej częścią Niziny Śląskiej .
Przepływa przez nią rzeka Biała .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 162 osoby .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 8 km od granicy z Czechami , w zachodniej części Kotliny Raciborskiej .
Należy do Euroregionu Pradziad .
