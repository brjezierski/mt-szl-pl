Wassenaar
Wassenaar ( ) – holenderskie miasto i gmina w prowincji Holandia Południowa .
Gmina ma 25.875 mieszkańców ( 31 maja 2009 , źródło : CBS ) i obejmuje obszar 62,50 km ². Gmina Wassenaar znajduje się w obszarze Haaglanden .
Wassenaar to miasto otoczone przez tereny leśne z dużą liczbą willi i rezydencji .
Wiele z nich to tradycyjne pozamiejskie , letnie domy zamożnych mieszkańców miast .
Oprócz wielu bogatych mieszkańców mieszczą się tu siedziby firm i konsulatów .
