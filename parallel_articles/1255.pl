Antonie Sminck Pitloo
Antonie lub Anton Sminck Pitloo ( ur .
21 kwietnia lub 8 maja 1790 w Arnhem , zm .
22 czerwca 1837 w Neapolu ) – holenderski malarz .
Studiował w Paryżu i Rzymie , gdzie przebywał na stypendium fundowanym przez Ludwika Bonaparte , króla Holandii .
Następnie został zaproszony przez księcia Grigorija Orłowa do Neapolu .
W 1820 ożenił się z Giulią Mori i został obywatelem Królestwa Obojga Sycylii .
Był wykładowcą Art Institute of Art w Neapolu , zmarł w czasie epidemii cholery w 1837 , został pochowany na Cimitero acattolico di Santa Maria della Fede .
Pitloo malował romantyczne pejzaże , uważany jest za prekursora impresjonizmu .
Oprócz obrazów olejnych malował też akwarele .
