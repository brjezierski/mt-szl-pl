Antony ( rejon dokszycki )
Antony ( ; ) – wieś na Białorusi , w obwodzie witebskim , w rejonie dokszyckim , w sielsowiecie Dokszyce .
Historia .
W czasach zaborów w granicach Imperium Rosyjskiego .
W latach 1921–1945 wieś leżała w Polsce w Polsce , w województwie wileńskim , w powiecie duniłowickim , a od 1926 w powiecie dziśnieńskim , w gminie Dokszyce .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwały tu 64 osoby , 1 była wyznania rzymskokatolickiego , a 63 prawosławnego .
Jednocześnie wszyscy mieszkańcy zadeklarowali białoruską przynależność narodową .
Było tu 11 budynków mieszkalnych .
W 1931 w 12 domach zamieszkiwały 63 osoby .
Miejscowość należała do parafii rzymskokatolickiej i prawosławnej w Dokszycach .
Podlegała pod Sąd Grodzki w Dokszycach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Dokszycach .
