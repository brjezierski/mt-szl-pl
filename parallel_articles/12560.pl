Wawrzyńcowice
Wawrzyńcowice ( dodatkowa nazwa w j. niem .
Lorenzdorf ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Strzeleczki .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
1 października 1948 r. nadano miejscowości , wówczas administracyjnie związanej z Zawadą i należącej do powiatu prudnickiego , polską nazwę Wawrzyńcowice .
Mieszkańcy .
Miejscowość zamieszkiwana jest przez mniejszość niemiecką oraz Ślązaków .
Mieszkańcy wsi posługują się gwarą prudnicką , będącą odmianą dialektu śląskiego .
Należą do podgrupy gwarowej nazywanej `` Klocołrzy '' .
