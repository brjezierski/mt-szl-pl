Wał ( maszynoznawstwo )
Wał – część maszyny , najczęściej w kształcie walca , obracająca się wokół własnej osi wraz z zamocowanymi na niej elementami i służąca do przenoszenia momentu obrotowego .
Na wale mogą być osadzone koła zębate , piasty , tarcze hamulcowe i inne elementy .
Głównym zadaniem wału jest przenoszenie ruchu obrotowego .
Ponadto wał może być obciążony siłami poprzecznymi i wzdłużnymi .
Zatem wał może być narażony jednocześnie na skręcanie , zginanie , ścinanie i rozciąganie lub ściskanie .
