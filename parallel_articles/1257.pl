Antonín Dvořák
Antonín Leopold Dvořák ( ur .
8 września 1841 w Nelahosevsi , zm .
1 maja 1904 w Pradze ) – czeski kompozytor , dyrygent i pedagog .
Był czołowym przedstawicielem czeskiej muzyki narodowej ( obok Bedřicha Smetany ) .
Komponował symfonie ( najbardziej znana `` Z Nowego Świata '' , 1893 ) , kwartety smyczkowe , opery , poematy symfoniczne , pieśni , msze , utwory fortepianowe oraz koncerty – w tym najsłynniejszy Koncert wiolonczelowy h-moll op .
104 , o którym Johannes Brahms pisał : „ Gdybym wiedział , że wiolonczela jest zdolna do takich rzeczy , sam bym napisał ten koncert ” .
Życiorys .
Antonín Dvořák urodził się i dorastał w miejscowości Nelahozeves niedaleko Pragi .
Jego ojciec František był rzeźnikiem .
W latach 1853–1856 młody Antonín Dvořák przebywał w miejscowości Zlonice , gdzie zaopiekował się nim miejscowy kantor i organista Antonín Liehmann , który uczył go gry na skrzypcach , fortepianie i organach .
W wieku 16 lat przeniósł się do Pragi , by studiować grę organową w Praskiej Szkole Organowej .
Jednocześnie grał na altówce w orkiestrze Teatru Tymczasowego ( późniejszego praskiego Teatru Narodowego ) pod kierownictwem Bedřicha Smetany .
Bardzo wcześnie rozpoczął swoją działalność kompozytorską .
17 listopada 1873 wziął ślub z Anną Čermákovą .
Żona Dvořáka zwróciła się o ocenę prac męża do Johannesa Brahmsa , który polecił Dvořáka berlińskiemu wydawcy Simrockowi .
To właśnie dla niego Dvořák napisał w 1878 pierwszą część `` Tańców Słowiańskich '' , która zyskała entuzjastyczne recenzje .
Zajął się również dyrygenturą .
W 1884 został zaproszony do Londynu , aby poprowadzić swoje `` Stabat Mater '' , dzieło wokalno-instrumentalne , napisane po śmierci jednej z córek .
W 1891 w Birmingham miało miejsce prawykonanie `` Requiem '' pod dyrekcją kompozytora ( czeska premiera miała miejsce w Národním divadle w Pradze w kwietniu 1892 ) .
Dzięki licznym sukcesom Dvořák otrzymał doktorat w Pradze i Cambridge oraz w konserwatorium praskim , gdzie wychował wielu znakomitych czeskich kompozytorów jak Vítězslav Novák i Josef Suk .
W 1892 Dvořák został zaproszony do Stanów Zjednoczonych .
Założycielka Narodowego Konserwatorium w Nowym Jorku , Jeanette Thurber chciała pozyskać Dvořáka jako kierownika tejże instytucji .
Początkowo wahający się kompozytor przyjął w końcu tę propozycję ; w czasie pobytu w Stanach skomponował w 1895 dziewiątą symfonię , jedno ze swych najgłośniejszych dzieł .
