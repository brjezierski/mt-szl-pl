Weird Al Yankovic
Weird Al Yankovic , właśc .
Alfred Matthew Yankovic ( ur .
23 października 1959 w Lynwood w Kalifornii ) – amerykański muzyk , satyryk , parodysta , akordeonista i producent telewizyjny .
Jest głównie znany ze swoich dowcipnych piosenek na tematy popkulturowe i parodii znanych utworów innych artystów .
Życiorys .
Zaczął grać na akordeonie na dzień przed siódmymi urodzinami , osiągając biegłość na tym instrumencie w wieku dziesięciu lat .
Al , opowiadał , że jako dziecko pobierał lekcje gry na akordeonie , ponieważ jego rodzice znali twórczość wokalisty i akordeonisty , `` amerykańskiego króla polki '' Frankie Yankovica i postanowili , że „ powinien być jeszcze jeden grający na akordeonie Yankovic na świecie ” .
Muzycy nie byli spokrewnieni , ale w pewnym okresie współpracowali ze sobą .
Al wykonał partię akordeonu w słynnym przeboju `` Who Stole the Kishka ? ''
na jednym z ostatnich albumów Frankiego `` Songs of the Polka King , Vol .
1 '' .
Fragment utworu Frankiego „ The Tick Tock Polka ” znajduje się w utworze „ Polka Face ” na płycie `` Weird Al 's Alpocalypse '' .
Gdy usłyszał radiowe show Dr. Demento w 1976 roku wysłał mu kasetę z nagraniem swojej piosenki pt .
`` Belvedere Cruising '' .
Od tego zaczęła się jego kariera .
Trzy lata później Al był studentem architektury i DJ-em w radiu uniwersyteckim .
Na antenie używał pseudonimu Weird Al , który stosuje do dziś .
Kiedy znany z przeboju `` My Sharona '' zespół The Knack miał przyjechać na uniwersytet Cat Poly , Al zagrał na akordeonie parodię tego hitu zatytułowaną `` My Bologna '' .
Spodobała się ona zespołowi , który doprowadził do wydania jej przez wytwórnię płytową , co dało Alowi sześciomiesięczny kontrakt .
Słuchacze Dr. Demento umieścili to nagranie na czele listy przebojów `` Funny five '' .
