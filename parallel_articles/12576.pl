Weisenbach
Weisenbach – miejscowość i gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Mittlerer Oberrhein , w powiecie Rastatt , wchodzi w skład wspólnoty administracyjnej Gernsbach .
Leży nad rzeką Murg , ok. 18 km na południowy wschód od Rastatt , przy drodze krajowej B462 .
