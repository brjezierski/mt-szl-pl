Weissach ( Badenia-Wirtembergia )
Weissach – miejscowość i gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Stuttgart , w powiecie Böblingen .
Leży w Heckengäu , ok. 20 km na północ od Böblingen , zaczyna się tutaj linia kolei zabytkowej Elias do Korntal-Münchingen .
Gmina składa się z dwóch dzielnic : Weissach i oddalonego o ok. 1 km Flacht .
W Weissach zlokalizowane jest Centrum Rozwojowe Porsche - `` Porsche Entwicklungszentrum '' wraz z torem testowym .
