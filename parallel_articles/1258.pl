Antracyt ( węgiel )
Antracyt – skała organogeniczna stanowiąca odmianę węgla kopalnego .
Jest to najsilniej przeobrażona odmiana węgla kamiennego , charakteryzująca się największą zawartością węgla i − co za tym idzie − najwyższą wartością energetyczną ze spalania .
Charakterystyka .
Jest skałą zbitą i twardą , zawiera niewielkie ilości węglowodorów , niekiedy także pirytu i kwarcu .
Występowanie .
Antracyt powstaje w wyniku działania bardzo wysokich temperatur i olbrzymiego ciśnienia ; podczas procesów diagenetycznych oraz metamorficznych .
Jego złoża występują na dużych głębokościach , głównie w osadach karbońskich .
Miejsca występowania – zagłębia karbońskie : ( Wielka Brytania ) , Francja , Belgia , Niemcy , Ukraina , USA , Kanada .
Także w utworach permskich : Chiny , Australia , Syberia .
W Polsce – w okolicach Wałbrzycha i Nowej Rudy – ( eksploatacja nie jest już obecnie prowadzona ) .
Wydobycie w Polsce .
Antracyt był w Polsce wydobywany w latach 1993−1998 z jedynego udokumentowanego w kraju złoża Wałbrzych-Gaj .
Po likwidacji KWK Wałbrzych i KWK Victoria antracyt skreślono z listy zasobów geologicznych Polski .
Zastosowanie .
Ze względu na powolne spalanie , małą ilość dymu i małe pylenie stosowany był do ogrzewania budynków ; współcześnie został wyparty przez inne formy ogrzewania .
