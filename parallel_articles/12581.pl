Wellingborough
Wellingborough – miasto w Wielkiej Brytanii , w środkowej części Anglii , w regionie East Midlands i hrabstwie Northamptonshire , w dystrykcie ( `` unitary authority '' ) North Northamptonshire .
Położone nad rzeką Nene , 18 km od siedziby hrabstwa ( Northampton ) oraz 13 km na południe od Kettering .
Odległość od Londynu wynosi ok. 110 km .
W 2001 roku miasto liczyło 46 959 mieszkańców .
Początki historii miasta szacowane są na VI w .
n.e. , a obecny charakter miejski nadał mu w 1201 roku Jan bez Ziemi .
W Wellingborough nie ma obecnie ani jednego samodzielnego kina .
Najbliższe to Odeon w Kettering i Vue w Northampton .
