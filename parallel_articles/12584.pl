Welshpool
Welshpool ( wal .
`` Y Trallwng '' ) – miasto we wschodniej Walii , w hrabstwie Powys ( historycznie w Montgomeryshire ) , położone na zachodnim brzegu rzeki Severn .
W 2011 roku miasto liczyło 6664 mieszkańców .
Miasto uzyskało przywilej targowy w 1263 roku .
Lokalna gospodarka w dużej mierze opiera się na rolnictwie .
Odbywa się tu targ zwierzęcy .
Na południe od Welshpool znajduje się XII-wieczny zamek Powis Castle .
W mieście swój wschodni kraniec ma linia kolei wąskotorowej Welshpool and Llanfair Light Railway wiodąca do Llanfair Caereinion .
