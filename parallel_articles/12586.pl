Mosty Wielkie
Mosty Wielkie ( dawniej `` Augustów '' , `` Mostki '' , `` Mosty '' , ukr .
`` Великі Мо́сти '' – Wełyki Mosty , hebr .
`` מוסט רבתי '' , niem .
`` Groß-Mosty '' ) – miasto na Ukrainie , w obwodzie lwowskim , w rejonie czerwonogrodzkim .
6256 mieszkańców ( 2020 ) , dla porównania w 2001 było ich 5924 .
Miasto królewskie Mosty lokowane w 1549 roku położone było w XVI wieku w województwie bełskim .
Wchodziło w skład starostwa mostowskiego w XVIII wieku .
Do 1945 w Polsce , w województwie lwowskim , w powiecie żółkiewskim , siedziba gminy Mosty Wielkie .
Mosty Wielkie leżą około 45 km od granicy z Polską .
Historia .
Miasto należało do dóbr koronnych , dzierżawy królewskie , w powiecie i województwie bełskim tworzącym starostwo .
