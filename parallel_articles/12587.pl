Żytyń Wielki
Żytyń Wielki ( , `` Wełykyj Żytyn '' ) – miejscowość na Ukrainie , w obwodzie rówieńskim , na Wołyniu .
Wieś znajduje się na północny wschód od miasta Równe .
Historia .
W okresie międzywojennym Żytyń Wielki był położony w granicach Polski , w województwie wołyńskim , w pow .
równieńskim , w gminie Aleksandrja .
Do 17 września 1939 roku znajdował się tu garnizon macierzysty Batalionu KOP „ Żytyń ” .
Od września 1939–1941 znalazł się pod okupacją sowiecką , a później od 1941–1944 pod okupacją niemiecką .
W latach 1945–1991 miejscowość znajdowała się w Ukraińskiej SRR .
Inne .
Znajdująca się tu parafia rzymskokatolicka została erygowana przez biskupa diecezji łuckiej w roku 1934 .
Miejscowość jest także siedzibą parafii prawosławnej , której świątynia pochodzi z 1767 .
Pobliska cukrownia w osiedlu „ Żytyń-Cukrownia ” należała do Jana Blocha , polskiego przedsiębiorcy żydowskiego pochodzenia .
W Żytyniu Wielkim urodził się Leonid Krawczuk , pierwszy prezydent Ukrainy .
