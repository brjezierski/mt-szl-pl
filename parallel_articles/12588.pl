21 maja 2020 r. swoją pracę rozpoczęło 15 grup inicjatywnych , zarejestrowanych przez Centralną Komisję Wyborczą Białorusi .
Do zarejestrowania kandydata w wyborach wymaganych było zebranie do 19 czerwca 2020 r. co najmniej 100 tys .
