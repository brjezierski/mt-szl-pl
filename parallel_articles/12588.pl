Wybory prezydenckie na Białorusi w 2020 roku
Wybory prezydenckie na Białorusi w 2020 roku – sfałszowane wybory prezydenckie , które odbyły się 9 sierpnia 2020 , w wyniku których wyłoniony został prezydent Białorusi .
8 maja 2020 Centralna Komisja Wyborcza podała do wiadomości kalendarz wyborczy .
Zgodnie z nim , do 15 maja włącznie istniała możliwość złożenia wniosków o rejestrację grup inicjatywnych .
Zbiórka 100 tysięcy podpisów , wymaganych w celu zarejestrowania danej kandydatury , była organizowana od 21 maja do 19 czerwca włącznie .
