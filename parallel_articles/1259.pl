Przedpokój
Przedpokój – pomieszczenie z podstawową funkcją komunikacyjną ( łączy pomieszczenia o odrębnych funkcjach ) podobnie jak korytarz .
Od korytarza różni się proporcjami : przedpokój powinien posiadać kształt kwadratu lub prostokąta , rzadziej nieregularny np .
w kształcie litery `` L '' .
Kształt i wymiary przedpokoju powinny umożliwiać przeniesienie chorego na noszach oraz wykonanie manewru wózkiem inwalidzkim w miejscach zmiany kierunku ruchu .
Przedpokoje stanowiące komunikację wewnętrzną w mieszkaniu powinny mieć szerokość w świetle minimum 1,2m .
Jak sama nazwa wskazuje przedpokój usytuowany jest `` przed '' pokojami .
Najczęściej stanowi łącznik pomiędzy pokojami a wyjściem z mieszkania , lokalu itd .
Przedpokój stanowi również pomieszczenie pozwalające na stopniowanie prywatności przestrzeni wewnątrz mieszkania .
Gradacja prywatności odbywa się dzięki otwarciu przedpokoju na przestrzeń publiczną ( salon ) , półpubliczną ( kuchnia ) i zamknięciu przestrzeni prywatnej ( sypialnia ) .
Przedpokój o dodatkowej funkcji reprezentacyjnej nazywany jest holem lub westybulem .
Przedpokój najprawdopodobniej powstał wskutek ewolucji sieni w budownictwie wiejskim .
Funkcję sieni przejęły dwa pomieszczenia : wiatrołap ( przedsionek ) oraz przedpokój .
W architekturze pałacowej powstała szczególna odmiana przedpokoju , antykamera .
Przykład przedpokoju w architekturze mieszczańskiej znaleźć można w Kamienicy Hipolitów .
