Wenatchee
Wenatchee – miasto ( `` city '' ) , ośrodek administracyjny hrabstwa Chelan , w środkowej części stanu Waszyngton , w Stanach Zjednoczonych , położone u podnóża Gór Kaskadowych , na zachodnim brzegu rzeki Kolumbia , nieopodal ujścia do niej rzeki Wenatchee , naprzeciw miasta East Wenatchee .
W 2013 roku miasto liczyło 32 701 mieszkańców .
Pierwsi osadnicy przybyli tutaj w 1888 roku , oficjalne założenie miejscowości nastąpiło w 1892 roku .
W 1902 roku Wenatchee uzyskało prawa miejskie .
Miasto rozwinęło się jako ośrodek produkcji owoców ( w szczególności jabłek ) , aluminium oraz przemysłu drzewnego .
Stanowi także bazę turystyczną dla ośrodków we wschodnich Górach Kaskadowych .
W mieście znajduje się siedziba lasu narodowego Wenatchee .
W mieście funkcjonuje uczelnia Wenatchee Valley College oraz ogrodniczy ośrodek badawczy należący do Washington State University .
Nazwa miasta pochodzi od słowa `` wenatchi '' , które w języku Indian Yakima oznacza „ rzekę wypływającą z kanionu ” .
