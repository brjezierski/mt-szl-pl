Afryka Wschodnia
Afryka Wschodnia – umowny region geograficzny w Afryce obejmujący tereny na wschód od Nilu i Wielkiego Rowu Afrykańskiego , w tym wyspy na Oceanie Indyjskim , tradycyjnie zaliczane do Afryki .
