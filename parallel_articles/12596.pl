Czasem do Afryki Wschodniej zalicza się też Mozambik , Sudan i Sudan Południowy .
Z drugiej strony termin ten bywa też niekiedy stosowany jedynie na określenie państw położonych wokół Jeziora Wiktorii , a więc Kenii , Tanzanii , Ugandy , czasem też Rwandy i Burundi .
