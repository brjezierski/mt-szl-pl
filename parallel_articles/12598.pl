West Bromwich
West Bromwich – miasto w zachodniej Anglii , położone w hrabstwie West Midlands .
W 2001 roku miasto liczyło 136 940 mieszkańców .
Siedziba klubu piłkarskiego West Bromwich Albion .
Miasto specjalizujące się w produkcji stali .
Produkcja stali wpłynęła znacznie na rozwój przemysłu metalurgicznego .
Najwięksi producenci kas pancernych i sejfów pochodzą właśnie z tych okolic .
Przykładem może być firma Samuela Withersa , założona w 1843 roku obecnie wchodząca w skład szwedzkiego holdingu Gunnebo , będącego jednym z największych producentów zabezpieczeń mechanicznych na świecie .
W tej miejscowości powstała także nieistniejąca już firma motoryzacyjna Jensen Motors .
