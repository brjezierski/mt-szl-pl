West Hartford
West Hartford – miejscowość w Stanach Zjednoczonych , w stanie Connecticut .
Leży w centrum stanu , na zachód od jego stolicy Hartford .
W jego bezpośrednim sąsiedztwie znajdują się Bloomfield , Newington , Farmington oraz Avon .
West Hartford jest położone w odległości mniej więcej 160 km na pd-zach od Bostonu i ok. 160 km na pn-wsch od Nowego Jorku .
