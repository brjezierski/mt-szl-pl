West Hollywood
West Hollywood – miasto w Stanach Zjednoczonych , w stanie Kalifornia , w hrabstwie Los Angeles .
Utworzone w 1984 r. , graniczy z Beverly Hills i Los Angeles .
Miasto jest jednym z głównych ośrodków kultury gejowskiej i ruchu na rzecz równouprawnienia osób bez względu na orientację seksualną .
Geje i lesbijki stanowią ponad jedną trzecią mieszkańców .
W 1985 roku miasto jako pierwsze w Stanach Zjednoczonych umożliwiło rejestrację związków partnerskich przez pary tej samej płci .
W trakcie Halloween odbywa się tu barwna parada przyciagająca wielu turystów i mieszkańców niezależnie od orientacji seksualnej .
Co roku odbywa się też duża parada z okazji święta dumy gejowskiej w Los Angeles ( `` LA Pride – Christopher Street West '' ) , w której biorą udział także politycy , w tym burmistrz Los Angeles , i inne znane osobistości .
