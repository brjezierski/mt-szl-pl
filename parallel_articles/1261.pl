Jest drugim co do wielkości ośrodkiem miejskim w Belgii , po regionie stołecznym Brukseli .
Wraz z miastami : Berchem , Berendrecht-Zandvliet-Lillo , Borgerhout , Deurne , Ekeren , Hoboken , Merksem , Wilrijk tworzy aglomerację .
W 2011 roku otrzymało tytuł Europejskiej Stolicy Młodzieży .
Dane ogólne .
Centrum przemysłowe , handlowe , finansowe i naukowe , m.in .
uniwersytetu ( w 2003 roku połączyły się trzy niezależne instytucje o randze uniwersyteckiej ) , Akademia Sztuk Pięknych , Konserwatorium Królewskie , Instytut Medycyny Tropikalnej , Akademia Morska .
Siedziba 40 banków , wielu towarzystw ubezpieczeniowych , spółek akcyjnych , giełdy .
W mieście działa polski konsulat honorowy .
Historia .
Do XV wieku .
Dzięki wykopaliskom wiadomo , że zakole rzeki tak jak wiele miast flamandzkich było zamieszkiwane już w czasach rzymskich , czyli w II i III wieku .
Antwerpia wyrosła z dwóch osad : `` aanwerp '' i Caloes , położonych 500 metrów na południe .
Fortyfikacja została zbudowana w VII wieku .
W tym samym czasie rozpoczęła się też chrystianizacja tego obszaru .
