Wesółka ( Wysocice )
Wesółka – część wsi Wysocice w Polsce , położona w województwie małopolskim , w powiecie miechowskim , w gminie Gołcza .
Wierni kościoła rzymskokatolickiego należą do parafii pw .
św .
Mikołaja w Wysocicach .
W latach 1975–1998 Wesółka administracyjnie należała do województwa krakowskiego .
