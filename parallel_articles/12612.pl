Wetlina ( rzeka )
Wetlina ( Wetlinka ) – rzeka w Bieszczadach Zachodnich ; prawy , największy dopływ Solinki .
Wetlina ma źródła pod Przełęczą Wyżną ( ok. 840 m n.p.m .
) , w górnym odcinku ( powyżej wsi Wetlina ) płynie przez Bieszczadzki Park Narodowy , a dalej przez Ciśniańsko-Wetliński Park Krajobrazowy .
W dolnym biegu , aż do ujścia , objęta jest rezerwatem Sine Wiry .
Odcinek od źródła do ujścia Górnej Solinki bywa nazywany Wetlinka ; niektóre źródła nazwę tę rozszerzają nawet na całą rzekę .
