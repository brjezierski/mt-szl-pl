Wewak
Wewak – miasto w Papui-Nowej Gwinei , stolica prowincji Sepik Wschodni , na północnym wybrzeżu Nowej Gwinei , na zachód od rzeki Sepik .
W mieście znajduje się niewielki port morski oraz port lotniczy Wewak .
Eksportuje się kawę , kakao , a także kauczuk .
Podczas II wojny światowej w mieście znajdowała się największa na wyspie japońska baza lotnicza , będąca częstym celem ataków samolotów amerykańskich i australijskich .
Siły japońskie stacjonujące w mieście skapitulowały 13 września 1945 roku .
