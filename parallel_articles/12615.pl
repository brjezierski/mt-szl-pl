Wiewiórka pospolita
Wiewiórka pospolita ( `` Sciurus vulgaris '' ) – gatunek gryzonia z rodziny wiewiórkowatych ( Sciuridae ) .
Systematyka .
Różnice w barwie futra i morfologii doprowadziły do wyodrębnienia 23 podgatunków :
Budowa ciała .
Tułów wraz z głową wiewiórki pospolitej osiągają łącznie długość 20–24 cm , ogona 17–20 cm , przy masie ciała 200–300 gramów .
Zwierzę charakteryzuje się ubarwieniem zmiennym .
Spotykane są dwie odmiany : jedna z częścią grzbietową wybarwioną na rudo , szarymi bokami i białą częścią brzuszną , a druga o grzbiecie czarnobrunatnym i białej części brzusznej , a bokach cieniowanych .
Możliwe są także wybarwienia pośrednie .
Wspomniane wersje kolorystyczne mogą występować równolegle u rodzeństwa z jednego miotu .
Jesienią wiewiórki zmieniają futro na bardziej gęste i wybarwione w szarym odcieniu .
Kolejna zmiana futra następuje wiosną .
Ogon pokryty jest włosem rozmieszczonym w dwóch pasmach .
Wiewiórka ma długie uszy zakończone kitkami .
Zęby mają niskie korony .
