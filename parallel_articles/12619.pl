Whakatane
Whakatane – miasto w Nowej Zelandii na Wyspie Północnej leżące nad Zatoką Obfitości .
Whakatane leży 90 kilometrów na wschód od Taurangi i 98 kilometrów na północny wschód od Rotorua przy ujściu rzeki Whakatane .
Miasto liczy 18 000 mieszkańców a kolejne 15 000 ludzi mieszka w rozległym rejonie Whakatane .
Z tego około 40 % ludności pochodzi od Maorysów .
