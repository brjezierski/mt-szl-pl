Wichita ( Indianie )
Wichita ( nazwa własna : `` Towihaedshi '' ) – plemię Indian Ameryki Północnej z grupy Kaddo , blisko spokrewnione z Paunisami .
Historia .
W dawnych czasach prowadzili koczowniczy tryb życia przemieszczając się od środkowego biegu rzeki Arkansas w stanie Kansas a rzeką Brazos w Teksasie .
Znani są od roku 1541 , kiedy to zjawili się na ich terenach Hiszpanie pod wodzą Coronada .
Hiszpanie nazwali ziemie Wiczitów „ prowincją Qiuvira ” bogatą rzekomo w złoto .
W rzeczywistości niczego takiego nie znaleźli .
Wiczitowie przeszli dwie epidemie ospy , które ich zdziesiątkowały .
Pierwszy traktat z rządem USA zawarli w 1835 roku .
