Wicklow ( irl .
`` Cill Mhantáin '' ) – miasto we wschodniej Irlandii w prowincji Leinster , stolica hrabstwa Wicklow .
Położone na wybrzeżu Morza Irlandzkiego , posiada port handlowy .
Miasto w 2011 roku zamieszkiwało 6761 mieszkańców .
Z Wicklow pochodzi Elaine Cassidy , irlandzka aktorka .
