Anuradhapura ( dystrykt )
Dystrykt Anuradhapura ( syng .
අනුරාධපුර දිස්ත්‍රික්කය , `` Anurādhapura distrikkaya '' ; tamil .
அனுராதபுரம் மாவட்டம , `` Aṉurātapuram māvaṭṭam '' ; ang .
`` Anuradhapura District '' ) – jeden z 25 dystryktów Sri Lanki położony w północnej części Prowincji Północno-Środkowej .
Stolicą jest miasto Anuradhapura , zamieszkane przez 63 208 mieszkańców ( 2011 ) .
Administracyjnie dystrykt dzieli się na dwadzieścia dwa wydzielone sekretariaty .
Ludność .
W 2012 roku populacja dystryktu wynosiła 860 575 osób .
Dominującą grupę ludności tworzą Syngalezi 90,7 % , a Maurowie lankijscy stanowią 8,3 %
Największą grupą religijną tworzą wyznawcy buddyzmu 90 % i islamu 8,4 % .
