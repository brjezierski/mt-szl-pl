Wieża obserwacyjna
Wieża obserwacyjna – wieża przeznaczona do obserwacji otaczającego ją terenu .
Z reguły wieże obserwacyjne są usytuowane w najwyższym punkcie okolicy .
Potocznie wieże obserwacyjne utożsamiane są z `` wieżami widokowymi '' jako punktami widokowymi , sztucznie wyniesionymi w terenie w celu umożliwienia obserwacji walorów widokowych i estetycznych terenu .
Wieże obserwacyjne mają szerokie zastosowanie i mogą być wykorzystywane do obserwacji :
Jako wieże obserwacyjne wykorzystuje się również latarnie morskie , wieże ciśnień , ratuszowe , kościelne , obronne , telekomunikacyjne , w tym telewizyjne itp .
