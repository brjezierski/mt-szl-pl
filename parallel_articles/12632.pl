Sterowanie i możliwości .
Sterowanie wózka widłowego obejmuje sterowanie układem jezdnym , a więc ruchem samego wózka , oraz sterowanie masztem i widłami które obejmuje najczęściej 3 ruchy : 1. ruch celem podnoszenia , opuszczanie wideł , 2. ruch przechylania masztu do siebie i od siebie , 3. ruch przesuwania karetki z widłami na boki w prawą lub lewą stronę .
Możliwa jest również zmiana rozstawu wideł ( pozycjonowanie ) .
Ruchy te nazywają się ruchami roboczymi wózka .
Kierowanie wózkiem .
Urządzenia sterujące ruchem wózka są podobne do samochodowych – występuje kierownica oraz pedały gazu i hamulca , często też , choć nie zawsze , sprzęgło .
Zależne jest to od zastosowanego układu napędowego :
W większości wózków są jedynie dwa biegi ( przełożenia ) - wolny i szybszy .
Dotyczą one obydwu kierunków jazdy ( naprzód i wstecz ) .
W niektórych modelach wózków z napędem hydrokinetycznym są tylko dwa pedały : jeden do jazdy w przód i jeden do jazdy w tył .
Prowadzenie wózka różni się znacznie od prowadzenia samochodu .
Wózek widłowy jest zwrotniejszy od samochodu , promień skrętu jest zdecydowanie mniejszy .
