Wózek jezdniowy
Wózek jezdniowy podnośnikowy nazywany zwyczajowo wózkiem widłowym – kołowy pojazd mechaniczny o napędzie silnikowym , przystosowany do przewozu materiałów o znacznej ( do kilkudziesięciu ton , zależnie od typu ) masie , stosowany w transporcie bliskim i magazynowaniu .
Używany jest głównie do prac rozładunkowych w magazynach , portach czy fabrykach .
Do transportu przy pomocy wózka widłowego materiał przeznaczony do przemieszczenia najczęściej umieszcza się na paletach .
