Wiedemar
Wiedemar – miejscowość i gmina w Niemczech , w kraju związkowym Saksonia , w powiecie Nordsachsen .
Do 31 grudnia 2012 wchodziła w skład związku gmin Wiedemar .
Do reformy administracyjnej w 2008 gmina leżała w powiecie Delitzsch , a do 29 lutego 2012 w okręgu administracyjnym Lipsk .
Gmina Wiedemar leży ok. 13 km na południowy zachód od miasta Delitzsch , na trasie autostrady A9 .
1 stycznia 2013 do gminy przyłączono gminy Neukyhna oraz Zwochau , które stały się automatycznie jej dzielnicami .
