Karłowice Wielkie
Karłowice Wielkie ( niem .
`` Groß-Karlowitz '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Kamiennik , leży na południowym zachodzie Polski w północno-wschodniej części Przedgórza Sudeckiego .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Karlowitz magnum '' w szeregu wsi lokowanych na prawie polskim `` iure polonico '' .
Historia .
We wczesnym średniowieczu tereny te zamieszkiwał lud Ślężan .
W latach pięćdziesiątych podczas prac związanych z budową szkoły podstawowej , odkryto na tym terenie cmentarzysko z urnami słowiańskimi .
W średniowieczu tereny te podlegały biskupstwu wrocławskiemu , od 1344 roku należały jako lenno do króla czeskiego .
W 1416 roku przeszły pod panowanie Habsburgów , a w roku 1748 tutejsze tereny wraz z wsią przeszły pod panowanie pruskie .
Do roku 1945 część wsi wraz z majątkiem obszarniczym nazywała się Kazischka , a obecnie cała wieś ma jedną nazwę Karłowice Wielkie .
