Malerzowice Wielkie
Malerzowice Wielkie ( niem .
`` Gross Mahlendorf '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie nyskim , w gminie Łambinowice .
W latach 1975–1998 wieś administracyjnie należała do ówczesnego województwa opolskiego .
