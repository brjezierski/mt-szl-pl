Przydroże Wielkie
Przydroże Wielkie ( niem .
`` Gross Schnellendorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Przysiółkiem wsi jest Drewnica .
Nazwa .
Według niemieckiego językoznawcy Heinricha Adamy ’ ego nazwa wywodzi się od polskiej nazwy drogi .
W swoim dziele o nazwach miejscowości na Śląsku wydanym w 1888 roku we Wrocławiu wymienia jako najstarszą zanotowaną nazwę `` Przydroszy '' podając jej znaczenie `` Dorf an der Strasse gelegen '' czyli w języku polskim `` Wieś leżąca przy drodze '' .
Niemcy początkowo zgermanizowali nazwę na `` Schnellendorf '' , a później na `` Gross Schnellendorf '' w wyniku czego utraciła ona swoje pierwotne znaczenie .
Po II wojnie światowej polska administracja wprowadziła nazwę `` Przydroże '' .
