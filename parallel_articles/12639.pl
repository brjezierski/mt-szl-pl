Wielkie Łąki ( jezioro )
Wielkie Łąki ( Jezioro Łąkie , kaszb .
`` Wiôldżé Łączi '' ) – przepływowe jezioro wytopiskowe położone na Pojezierzu Kaszubskim ( gmina Kartuzy , powiat kartuski , województwo pomorskie ) na skraju Kaszubskiego Park Krajobrazowego .
Jezioro jest połączone wąską strugą z jeziorem Małe Łąki .
Przesmykiem rozdzielającym oba jeziora prowadzi trasa szosy powiatowej z Kartuz przez Sianowo i Staniszewo do Mirachowa .
Powierzchnia całkowita 35,46 ha , maksymalna głębokość 6 m .
