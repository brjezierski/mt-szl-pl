Anyama
Anyama – miasto w Wybrzeżu Kości Słoniowej , stanowiące północne przedmieście Abidżanu , położone ok. 20 km od jego centrum .
Jego populacja w 2014 roku liczyła 103 297 mieszkańców .
Miasto wraz z okolicznymi terenami tworzy podprefekturę Anyama – jedną z czterech podprefektur w dystrykcie autonomicznym Abidżan .
Powierzchnia podprefektury Anyama wynosi 498 km² , w 2014 roku zamieszkiwało ją 148 962 osób
W mieście działa klub sportowy Rio Sport d'Ányama .
