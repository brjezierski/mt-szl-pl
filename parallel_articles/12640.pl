Wielki Kair
Wielki Kair ( arab .
القاهرة الكبرى , `` Al-Kahira al-Kubra '' ) - największy obszar metropolitarny w Egipcie i Afryce , a także 16. na świecie oraz 3. pod względem wielkości w tzw .
świecie islamskim ( po Stambule i Karaczi ) .
Cały obszar metropolitarny zajmuje ok. 1700 km² i liczy ok. 15 mln mieszkańców .
Obejmuje oprócz Kairu takie miasta jak Giza , Heluan , Imbaba , Banha , Szubra al-Chajma czy Madinat as-Sadis min Uktubar .
