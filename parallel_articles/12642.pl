Opis .
Jest jedną z największych ryb spędzających część życia w wodach słodkich .
Osiągała do 5 m długości i 1500 kg masy ciała ( maksymalna odnotowana masa ciała 2072 kg ) .
Obecnie rzadko spotyka się okazy przekraczające 200 kg .
Głowa jest krótka , szeroka , otwór gębowy szeroki , półksiężycowaty , wąsiki są płaskie , mięsiste , zaopatrzone w liczne , cienkie przydatki .
Na grzbiecie występuje szereg 11–15 tarczek kostnych , na bokach 40–60 tarczek , a na brzuchu 9–12 .
Odżywianie .
Młode okazy żywią się głównie bezkręgowcami dennymi , starsze polują na ryby .
