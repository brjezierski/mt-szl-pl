Bieługa
Bieługa , wyz ( `` Huso huso '' ) – gatunek wędrownej , anadromicznej ryby z rodziny jesiotrowatych ( Acipenseridae ) .
Występowanie .
Morze Kaspijskie , Morze Czarne , Morze Azowskie , wschodnia część Morza Śródziemnego i Morze Adriatyckie .
Na tarło wpływa głównie do rzek : Wołga , Ural , Terek , Kura , Dunaj , Dniestr , Don i Dniepr .
