Płużnica Wielka
Płużnica Wielka ( niem .
`` Groß Pluschnitz '' ) – wieś w Polsce położona w województwie opolskim , w powiecie strzeleckim , w gminie Strzelce Opolskie .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Niedaleko na wschód , lecz już w województwie śląskim leży Płużniczka , zwana dawniej Płużnicą Małą .
Nazwa .
W alfabetycznym spisie miejscowości na terenie Śląska wydanym w 1830 roku we Wrocławiu przez Johanna Knie wieś występuje pod polską nazwą `` Plusznica '' , oraz dwiema niemieckimi nazwami `` Groß Pluschnitz '' i `` Klein Pluschnitz '' .
Spis wymienia również inne części wsi jak `` Czerwona grobla '' oraz `` Grabina '' .
Ze względu na polskie pochodzenie nazwy w 1936 roku nazistowska administracja III Rzeszy zmieniła nazwę na nową , całkowicie niemiecką - `` Marklinden '' , którą nosiła do roku 1945 .
