Wiener Neustadt ( stolica tytularna )
Wiener Neustadt ( łac .
`` Dioecesis Neostadiensis '' ) – stolica historycznej diecezji w Austrii , erygowanej w roku 1468 , a zlikwidowanej w roku 1785 .
Współcześnie miejscowość Wiener Neustadt w kraju związkowym Dolna Austria .
Obecnie katolickie biskupstwo tytularne .
