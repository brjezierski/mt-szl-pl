Wiesbaden
Wiesbaden – miasto na prawach powiatu , uzdrowisko w środkowych Niemczech , od 1945 roku stolica kraju związkowego Hesja , w rejencji Darmstadt .
Leży nad rzeką Ren , u podnóża gór Taunus .
Znane jest ze źródeł termalnych .
Od 1945 do Wiesbaden należy Mainz-Kastel , poprzednio była to część Moguncji .
We wrześniu 2015 miasto liczyło mieszkańców .
