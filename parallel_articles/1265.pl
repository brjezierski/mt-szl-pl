Anonimowi Alkoholicy
Anonimowi Alkoholicy ( AA , ) – dobrowolne , samopomocowe grupy osób uzależnionych od alkoholu etylowego , tworzone w celu utrzymania trzeźwości własnej i wspomagania innych alkoholików w jej osiąganiu .
Podstawą filozofii AA jest dwanaście kroków AA .
Na dzień 1 stycznia 2013 roku `` General Service Office of Alcoholics Anonymous '' ( Główne Biuro Służb AA , w polskim AA używa się nazwy Biuro Usług Ogólnych ) oszacowało , że w 114 642 grupach spotyka się 2 131 549 członków .
Grupy spotykają się w co najmniej 170 krajach , a w 63 z nich funkcjonują autonomiczne biura służby krajowej .
