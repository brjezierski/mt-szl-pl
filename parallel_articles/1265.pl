Anonimowi alkoholicy są anonimowi , ponieważ wspólnota AA rządzi się zasadami , a nie jest rządzona przez osoby .
Anonimowość gwarantuje , że imię , nazwisko , praca , zawód nie zostaną ujawnione .
Wspólnota AA nie angażuje się w publiczne polemiki , nie zajmuje stanowiska w jakichkolwiek sporach .
Najważniejszym celem dla uczestników jest pozostać trzeźwym i pomagać innym alkoholikom osiągnąć trzeźwość .
Jakich działań nie podejmuje AA .
Anonimowi Alkoholicy pokazują jedynie , w jaki sposób uczestnicy sami nauczyli się żyć bez alkoholu .
Konsekwentnie nie zabraniają też swoim uczestnikom indywidualnego i prywatnego angażowania się w powyższe przedsięwzięcia zawodowo lub społecznie .
Historia .
Na świecie .
Miejscem założenia AA były Stany Zjednoczone .
Inicjatorem był makler giełdowy Bill Wilson .
Drugim współzałożycielem był dr Bob Smith .
Jego pierwszy dzień nieprzerwanej trzeźwości 10 czerwca 1935 roku uważa się za symboliczną datę narodzin ruchu .
Swoje korzenie organizacyjne ruch miał w grupach oksfordzkich .
