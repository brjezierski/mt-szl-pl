Wigan
Wigan – miasto położone w północno-zachodniej części Anglii między miastami Liverpool , Manchester i Preston , w obrębie hrabstwa Wielki Manchester , w dystrykcie Wigan .
W 2001 roku miasto liczyło 81 203 mieszkańców .
Inne sąsiednie miasta to Chorley , St Helens , Bolton i Warrington .
Wigan jest w Anglii znane z ( nieistniejącego od dawna ) molo , niegdyś istotnego punktu załadowczego drewna i węgla .
George Orwell umieścił molo w tytule książki `` Droga na molo w Wigan '' .
