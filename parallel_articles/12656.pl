Wiktar Zujeu
Wiktar Zujeu ( biał .
Віктар Зуеў , ros .
Виктор Зуев , `` Wiktor Zujew '' ; ur .
22 maja 1983 w Witebsku ) – białoruski bokser .
Wielokrotny mistrz Białorusi oraz medalista amatorskich mistrzostw świata i Europy w wadze ciężkiej i superciężkiej .
Srebrny medalista olimpijski z 2004 roku .
Karierę rozpoczynał w wadze ciężkiej ( do 91 kg ) .
Pierwszy znaczący międzynarodowy sukces osiągnął w 2002 roku na mistrzostwach Europy w Permie , gdy w wieku 19 lat zdobył brązowy medal .
Rok później zajął również 3. miejsce na mistrzostwach świata , a dwa lata później został wicemistrzem Europy .
Ukoronowaniem tych sukcesów było zdobycie w 2004 roku na igrzyskach olimpijskich w Atenach srebrnego medalu .
Zujeu ustąpił tam jedynie Odlanierowi Solísowi ( 13:22 ) .
Następne cztery lata były jednak dla Białorusina nieudane .
W 2008 roku w Pekinie został wyeliminowany już po pierwszej walce , przegrywając na punkty z Włochem Clemente Russo ( 1:7 ) .
Po igrzyskach przeszedł do wagi superciężkiej ( ponad 91 kg ) .
W nowej kategorii wagowej powrócił do światowej czołówki , zdobywając 3. miejsce na mistrzostwach świata w Mediolanie i 2. na mistrzostwach Europy w Moskwie .
W 2011 roku , broniąc w Ankarze wicemistrzostwa Europy , zmierzył się już w pierwszej rundzie z faworytem turnieju , Roberto Cammarelle .
Włoch pokonał go przed czasem po ciosach w głowę ( RSC-H ) .
