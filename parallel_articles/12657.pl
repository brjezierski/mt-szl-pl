W latach 1957–1960 służył w Armii Czerwonej .
Obronił doktorat nauk technicznych , następnie pracował m.in .
w rafinerii w Orsku i Zakładzie Uzdatniania Gazu w Orenburgu .
W latach 1985–1989 był ministrem przemysłu gazowego ZSRR , potem do 1992 roku szefem zarządu Gazpromu .
Od 1992 do 1998 był premierem Rosji .
Jako premier w dniach od 5 do 6 listopada 1996 pełnił zastępczo funkcję prezydenta Rosji ( na czas operacji Borysa Jelcyna ) .
Ponownie pełnił obowiązki premiera od sierpnia do września 1998 , po dymisji Siergieja Kirijenki .
W 1993 roku prezydent Jelcyn mianował go wiceprezydentem ( później stanowisko to zniesiono ) .
W 1995 roku Czernomyrdin został przewodniczącym partii Nasz Dom – Rosja .
Był przedstawicielem prezydenta Jelcyna ds .
konfliktu bałkańskiego oraz prezesem Rady Dyrektorów Gazpromu .
Od 2001 do 2009 roku , z nominacji prezydenta Władimira Putina , był ambasadorem Rosji na Ukrainie .
Ostatnią funkcją było stanowisko doradcy prezydenta Rosji ds .
współpracy gospodarczej z krajami poradzieckiej Wspólnoty Niepodległych Państw .
W drugiej połowie lat 90. wymieniany jako kandydat na następcę prezydenta Borysa Jelcyna .
Został odznaczony m.in .
Orderem Zasług dla Ojczyzny I , II , III i IV klasy ( 2009 , 1998 , 2008 , 2010 ) , Orderem Przyjaźni ( 2003 ) , Orderem Rewolucji Październikowej ( 1986 ) , Orderem Czerwonego Sztandaru Pracy ( 1979 ) , Orderem „ Znak Honoru ” ( 1974 ) oraz ukraińskimi Orderem Jarosława Mądrego V klasy ( 2003 ) i Orderem Zasługi III klasy ( 2009 ) .
