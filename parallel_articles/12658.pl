Wiktor Fiodorowicz Janukowicz ( ur .
9 lipca 1950 w Jenakijewem w obwodzie donieckim ) – ukraiński polityk , inżynier i wykładowca akademicki .
W latach 1997–2002 przewodniczący Donieckiej Obwodowej Administracji Państwowej , deputowany , od 2003 do 2010 lider Partii Regionów .
W latach 2002–2005 oraz 2006–2007 premier Ukrainy , od 2010 do 2014 prezydent Ukrainy .
Kandydat w wyborach prezydenckich w 2004 , w których został ogłoszony zwycięzcą .
