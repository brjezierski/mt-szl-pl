Wiktor Juszczenko
Wiktor Andrijowycz Juszczenko , ukr .
Віктор Андрійович Ющенко ( ur .
23 lutego 1954 w Chorużiwce ) – ukraiński ekonomista , polityk , w latach 1993–1999 prezes Narodowego Banku Ukrainy , w latach 1999–2001 premier Ukrainy , w latach 2005–2010 prezydent Ukrainy .
Życiorys .
Rodzina .
Jego matka Barbara ( 1918–2005 ) pracowała jako nauczyciel fizyki i matematyki .
Ojciec Andrij ( 1919–1992 ) był żołnierzem Armii Czerwonej , dostał się do niewoli niemieckiej , przeżył w niewoli do wyzwolenia .
Po wojnie pracował jako nauczyciel angielskiego .
