Wiktor Wasniecow
Wiktor Michajłowicz Wasniecow ( Виктор Михайлович Васнецов ; ur .
15 maja 1848 we wsi Łopiał , zm .
23 czerwca 1926 w Moskwie ) – malarz rosyjski , twórca obrazów o tematyce mitologicznej i historycznej .
Życiorys .
Dzieciństwo ( 1848–1858 ) .
Wiktor Wasniecow urodził się we wsi Łopiał ( gubernia wiatska , obecnie obwód kirowski ) w 1848 roku .
Jego ojciec , Michaił Wasiljewicz Wasniecow , był gruntownie wykształconym duchownym , interesował się astronomią , naukami przyrodniczymi oraz malarstwem .
Pradziadek artysty malował ikony .
Wiktor Wasniecow miał dwóch braci , z których Apolinary został również malarzem .
Wiatka ( 1858–1867 ) .
W wieku dziesięciu lat Wiktor zaczął naukę w seminarium duchownym znajdującym się w Wiatce ( obecnie Kirow ) .
Co roku w okresie letnim przenosił się z rodziną do bogatego kupieckiego miasteczka Riabowo .
Przez lata spędzone w seminarium Wiktor pracował dla miejscowego właściciela sklepu z ikonami .
Po zdobyciu dyplomu z seminarium Wiktor zdecydował się na wyjazd do Sankt Petersburga , aby tam studiować sztukę .
Artysta wystawiał swoje obrazy na aukcjach , aby uzbierać pieniądze wystarczające na wyjazd do stolicy .
Sankt Petersburg ( 1867–1876 ) .
W sierpniu 1867 roku Wiktor został przyjęty na Cesarską Akademię Sztuk Pięknych w Petersburgu .
