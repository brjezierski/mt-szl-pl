Wilamowice ( gmina w rejencji katowickiej )
Wilamowice ( ) – dawna gmina wiejska funkcjonująca w latach 1940–1945 pod okupacją niemiecką w Polsce .
Siedzibą gminy były Wilamowice ( `` Wilmesau '' ) .
Gmina Wilamowice ( Wilmesau ) powstała na obszarze ówczesnego powiatu bielskiego ( od 10 października 1939 pod nazwą Landkreis Bielitz ) , w części powiatu która przed wojną należała do powiatu bialskiego w woj .
