Alfabet wilamowski
Alfabet wilamowski – zmodyfikowany alfabet łaciński służący do zapisywania języka wilamowskiego ( wymysiöeryś ) .
Alfabet ten składa się z 34 znaków :
A , Ȧ , AO , B , C , Ć , D , E , F , G , H , I , J , K , Ł , L , M , N , Ń , O , Ö , P , Q , R , S , Ś , T , U , Ü , V , W , Y , Z , Ź , Ż .
w tym jednego dwuznaku :
AO
