Krzyż wilamowski
Krzyż wilamowski jest częścią stroju wilamowskiego .
Krzyżyk ten przypinany był do korali .
Korali noszono przeważnie 5 sznurków , zaś przy tym najdłuższym , dolnym czerwoną kokardką przywiązywany był właśnie ten równoramienny srebrny krzyż wielkości 4-6 cm .
z insygniami Męki Pańskiej i monogramami wyrytymi po jednej stronie , którą na wierzchu nosiły panny oraz cierniową koroną i napisem INRI z drugiej strony – noszonej wierzchem przez mężatki .
