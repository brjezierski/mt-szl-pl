Wilamowa
Wilamowa ( niem .
`` Alt Wilmsdorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Paczków .
W latach 1975–1998 miejscowość administracyjnie należała do województwa opolskiego .
Do miejscowości należy przysiółek Książe .
Nazwa .
12 lutego 1948 r. ustalono urzędową polską nazwę miejscowości – Wilamowa , określając drugi przypadek jako Wilamowej , a przymiotnik – wilamowski .
Historia .
Wieś powstała około 1300 roku , ale jest prawdopodobieństwo , że założono ją równocześnie z Paczkowem .
28 maja 1949 roku założono na terenie wsi Rolniczą Spółdzielnię Produkcyjną jedną z najlepszych spółdzielni rolniczych w Polsce .
Pierwszym prezesem RSP przez ponad 30 lat był Władysław Grabowski , poseł na sejm oraz odznaczony orderem Budowniczych Polski Ludowej .
