Wilczonek
Wilczonek – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Kotuń .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Aleksego w Oleksinie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Dane ogólne .
Wilczonek jest jedną z 32 miejscowości wchodzącą w skład gminy Kotuń .
Parafia : Oleksin .
Dane na rok 2008 : Liczba mieszkańców ok. 213 .
Liczba domów : 69 .
Historia .
Pochodzenie nazwy Wilczonek jest trudne do zbadania , ponieważ brak jest udokumentowanych na ten temat źródeł .
