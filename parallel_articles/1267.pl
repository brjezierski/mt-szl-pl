Apatin ( cyr .
Апатин ) – miasto w Serbii , w Wojwodinie , w okręgu zachodniobackim , siedziba gminy Apatin .
Leży nad Dunajem w regionie Baczka .
W 2011 roku liczyło 17 411 mieszkańców .
W mieście znajduje się stocznia rzeczna , port rybacki oraz browar .
Swą siedzibę ma tu klub piłkarski Mladost Apatin .
W mieście rozwinął się przemysł chemiczny , drzewny i włókienniczy .
