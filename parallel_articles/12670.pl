Wilemowice
Wilemowice – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Kamiennik .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Wylemowiczi '' w szeregu wsi lokowanych na prawie polskim `` iure polonico '' .
