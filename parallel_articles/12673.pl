Wilk szary
Wilk szary , wilk ( `` Canis lupus '' ) – gatunek drapieżnego ssaka z rodziny psowatych ( Canidae ) , zamieszkującego lasy , równiny , tereny bagienne oraz góry Eurazji i Ameryki Północnej .
Gatunek o skłonnościach terytorialnych .
Zwykle terytorium zajmowane przez watahę to 100–300 km² , ale wielkość ta zależy od dostępności pokarmu i terenu .
Może występować współużytkowanie części terenu przez dwie konkurujące watahy .
Wilk jest w stanie w dobę pokonać dystans kilkudziesięciu kilometrów .
W poszukiwaniu partnerki samiec potrafi w ciągu 2 tygodni przebyć ponad 600 km .
Stada wilków liczą do 20 osobników , zwykle jednak są znacznie mniej liczne , będąc grupą rodzinną składającą się z pary rodzicielskiej , młodych i ewentualnie części wilków z poprzedniego miotu .
Watahy mają ściśle określoną hierarchię wewnętrzną .
