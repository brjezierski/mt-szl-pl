Wilkes-Barre
Wilkes-Barre – miasto w Stanach Zjednoczonych , w hrabstwie Luzerne , w stanie Pensylwania , nad rzeką Susquehanna , w zespole miejskim Scranton–Wilkes-Barre .
W 2019 r. według spisu ludności miasto miało 40 766 mieszkańców .
W mieście rozwinął się przemysł obuwniczy , szklarski , meblarski , metalowy , lotniczy oraz elektroniczny .
