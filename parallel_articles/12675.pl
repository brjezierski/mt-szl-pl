Wilkoszewice ( przystanek kolejowy )
Wilkoszewice – jeden z przystanków kolejowych na linii kolejowej Warszawa Zachodnia – Katowice w Wilkoszewicach ( powiat piotrkowski ) .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Na przystanku zatrzymują się tylko pociągi osobowe .
W roku 2017 przystanek obsługiwał 10–19 pasażerów na dobę .
Na przełomie stycznia i lutego 2019 PKP podpisały z konsorcjum firm Helifactor i MERX umowę na budowę tzw .
innowacyjnego dworca systemowego w formie półotwartej poczekalni ogrzewanej promiennikami osłony .
