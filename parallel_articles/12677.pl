Wilkowo ( powiat kętrzyński )
Wilkowo ( niem .
`` Wilkendorf '' ) – wieś w Polsce , położona w województwie warmińsko-mazurskim , w powiecie kętrzyńskim , w gminie Kętrzyn .
W latach 1975–1998 miejscowość należała administracyjnie do województwa olsztyńskiego .
Wieś położona jest przy drodze wojewódzkiej nr 591 Kętrzyn – Mrągowo .
W Wilkowie dzieciństwo spędził : osobowość internetowa i kandydat na prezydenta Białegostoku Krzysztof Kononowicz .
