Willem Cornelisz Duyster
Willem Cornelisz Duyster ( wym .
; ur .
30 sierpnia 1599 w Amsterdamie , zm .
31 stycznia 1635 tamże ) - holenderski malarz .
Malował sceny rodzajowe i portrety , często w swoich dziełach przedstawiał żołnierzy , zwykle podczas zabawy , gier lub zalotów .
Osiągnął znaczną biegłość w przedstawianiu tekstyliów i różnic psychologicznych pomiędzy prezentowanymi postaciami .
Jego karierę przerwała przedwczesna śmierć w czasie epidemii dżumy w 1635 .
