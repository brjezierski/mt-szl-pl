William Cameron Townsend
William Cameron Townsend ( ur .
9 czerwca 1896 w Eastvale w stanie Kalifornia , zm .
23 kwietnia 1982 w Waxhaw w Karolinie Północnej ) – amerykański misjonarz i językoznawca , założyciel organizacji Wycliffe Bible Translators oraz Summer Institute of Linguistics ( obecnie znane pod nazwą SIL International ) , które w swojej długiej historii kładą nacisk na tłumaczenia Pisma Świętego na języki mniejszościowe oraz rozwój piśmiennictwa wśród tych języków .
Od początku Wycliffe Bible Translators przetłumaczyło Biblię na ponad tysiąc języków .
