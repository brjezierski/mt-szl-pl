William Shakespeare
William Shakespeare ( forma spolszczona : Szekspir ) ( ur .
prawdopodobnie 23 kwietnia 1564 , ochrzczony 26 kwietnia 1564 , w Stratford-upon-Avon , zm .
tamże ) – angielski poeta , dramaturg , aktor .
Powszechnie uważany za jednego z najwybitniejszych pisarzy literatury angielskiej oraz reformatorów teatru .
Napisał 38 sztuk , 154 sonety , a także wiele utworów innych gatunków .
Mimo że cieszył się popularnością już za życia , jego sława rosła głównie po śmierci , dopiero wtedy został zauważony przez prominentne osobistości .
Uważa się go za poetę narodowego Anglii .
Większość swoich prac napisał między 1586 a 1612 rokiem .
Przedmiotem ciągłej debaty jest ich chronologia , a nawet kwestia autorstwa większości z nich .
Był jednym z niewielu dramaturgów , którzy z powodzeniem tworzyli zarówno komedie , jak i tragedie .
Sztuki Shakespeare ’ a przetłumaczono na wiele języków nowożytnych , inscenizacje mają miejsce na całym świecie .
Jest również najczęściej cytowanym pisarzem anglojęzycznego świata .
Wiele jego neologizmów weszło do codziennego użycia .
Lista słów , które wprowadził do języka angielskiego , liczy około 1600 pozycji .
W swoich dziełach użył od 20 000 do 30 000 różnych słów .
