Willingen ( Upland )
Willingen – gmina uzdrowiskowa w Niemczech , w kraju związkowym Hesja , w rejencji Kassel , w powiecie Waldeck-Frankenberg , w górach Rothaargebirge , u podnóża Langenbergu .
Liczy 6070 mieszkańców ( 31.12.2015 ) .
Skocznia narciarska .
W miejscowości znajduje się skocznia narciarska Mühlenkopfschanze o punkcie konstrukcyjnym K-130 , która corocznie gości cykl Pucharu Świata .
Rekordzistą skoczni jest Klemens Murańka , który skoczył w kwalifikacjach w 2021 roku 153 m .
