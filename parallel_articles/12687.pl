Willy Hanft
Willy Hanft ( ur .
8 grudnia 1888 w Hanowerze , zm .
19 lipca 1987 w Norymberdze ) – niemiecki malarz .
Życiorys .
W latach 1908-1913 studiował malarstwo w Akademii Sztuk w Düsseldorfie ( `` Kunstakademie Düsseldorf '' ) pod kierunkiem profesorów Adolfa Männchena , Willego Spatza i Dietricha .
W latach 1920–1940 tworzył we własnej pracowni w dzielnicy Drezna - Blasewitz , oraz w Düsseldorfie .
W latach 1938–1944 jego prace wystawiano regularnie na Wielkiej Niemieckiej Wystawie Sztuki ( `` Grosse Deutsche Kunstausstellung '' ) , prezentująca „ Sztukę III Rzeszy ” , zgodną z linią ideologiczną III Rzeszy ) w Monachium :
Biorąc pod uwagę ilość i zakres tematyki stworzonych przez niego pejzaży należy wyciągnąć wniosek iż tworzył w wielu miejscach , lecz szczególnie w obszarze alpejskim .
W 1943 został w wyniku pomyłki wymieniony jako zmarły .
W latach 1945–1952 tworzył przede wszystkim w Dreźnie , w lipcu 1952 przeprowadził się do śródmieścia Drezna .
W 1977 owdowiał , w 1979 zawarł związek małżeński z Wilhelminą Meyer z Zirndorfu .
W maju 1983 przeprowadził się do Norymbergi , gdzie zmarł w 19 lipca 1987 .
Podstawą jego twórczości były pejzaże oraz martwe natury z kwiatami .
Pod względem techniki malarskiej koncentrował się na technice malarstwa olejnego .
