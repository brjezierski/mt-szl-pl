Aperatos
Aperatos ( gr .
Απέραθος ) lub Apirantos ( gr .
Απείρανθος ) – górzysta miejscowość w Grecji , na wyspie Naksos , w administracji zdecentralizowanej Wyspy Egejskie , w regionie Wyspy Egejskie Południowe , w jednostce regionalnej Naksos , w gminie Naksos i Mikres Kiklades .
W 2011 roku liczyła 722 mieszkańców .
