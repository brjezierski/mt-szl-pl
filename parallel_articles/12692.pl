Wilamowianie
Wilamowianie ( wil .
`` Wymysiöejyn '' , niem .
`` Wilmesauer '' ) – grupa etnograficzna zamieszkująca miasto Wilamowice koło Bielska-Białej , mówiąca własnym językiem wilamowskim , posiadająca własny strój ludowy i tradycje .
Nazwa `` Wilamowianie '' odnosi się do mieszkańców Wilamowic utożsamiających się ze społecznością kulturową Wilamowic , mieszkańców miasta określa się słowem `` wilamowiczanie '' .
Wilamowianie nie są oficjalnie uznawaną przez rząd grupą etniczną .
Mowa .
Wilamowianie posługują się językiem polskim oraz wilamowskim , nazywanym `` wymysiöeryś '' , który w powszechnym użytku był w Wilamowicach do 1945 .
Pod wpływem represji po zakończeniu II wojny światowej doszło do zerwania przekazu międzypokoleniowego .
Wymysiöeryś został w znacznej mierze zastąpiony przez język polski .
Obecnie tylko około 25 osób potrafi posługiwać się tym językiem .
Zainteresowanie kulturą oraz mową wilamowską zaczęło odżywać w latach 90 .
XX wieku , m.in .
za sprawą Tomasza Wicherkiewicza , który w 1998 opublikował rozprawę doktorską „ Język , kultura i mieszkańcy Wilamowic w świetle twórczości literackiej Floriana Biesika ” .
W 2003 roku w miejscowej szkole podstawowej rozpoczęto nauczanie języka wilamowskiego , którego podjął się miejscowy poeta Józef Gara .
Współcześnie grupa lokalnych aktywistów z Tymoteuszem Królem na czele zaangażowana jest w rewitalizację języka wilamowskiego , którą wspierają m.in .
naukowcy z Uniwersytetu Warszawskiego .
Dzięki wnioskowi Tymoteusza Króla , Biblioteka Kongresu USA 18 lipca 2007 wpisała język wilamowski w rejestr języków świata , został on również zarejestrowany w Międzynarodowej Organizacji Normalizacyjnej , gdzie przydzielono mu kod ISO : `` wym '' .
Od 2015 podjęto kilka prób przyznania wilamowskiemu statusu języka regionalnego w rozumieniu ustawy z dnia 6 stycznia 2005 r. o mniejszościach narodowych i etnicznych oraz o języku regionalnym .
