Windhuk
Windhuk ( ang .
`` Windhoek '' , wym .
; afr .
`` Windhoek '' , wym .
; niem .
`` Windhuk '' ) – stolica Namibii , położona w środkowej części kraju na wysokości 1650 m n.p.m .
Jest to także stolica regionu Khomas .
Na 645 km² mieszka 315 932 osób co daje 489,9 mieszkańców na km² .
Historia .
W 1840 Jonker Afrikaner założył osadę Orlam w miejscu , w którym znajduje się obecnie Windhuk .
On i jego zwolennicy przebywali w pobliżu jednego z głównych gorących źródeł , położonych w dzisiejszych przedmieściach `` Klein Windhoek '' .
Zbudował murowany kościół na 500 osób używany również jako szkoła .
Zostały założone ogrody i pola uprawne .
Dwóch misjonarzy : Carl Hugo Hahn i Franz Heinrich Kleinschmidt z `` Rheinische Missionsgesellschaft '' rozpoczęło tam pracę pod koniec roku 1842 .
Dwa lata później dwóch metodystów – Richard Haddy i Joseph Tindall – zostało zaproszonych przez Jonkera , aby zmienić Hahna i Kleinschmidta , którzy nie zgadzali się z polityką Jonkera .
Przez pewien czas Windhuk dobrze prosperowało , ale wojny między narodami Nama i Herero w końcu zniszczyły osadę .
Po długiej nieobecności , Hahn odwiedził ponownie Windhuk w 1873 roku i był przerażony widząc , że nic nie pozostało z dawnego miasta .
