Winnetka
Winnetka – wieś ( `` village '' ) w hrabstwie Cook , w północno-wschodniej części stanu Illinois , w Stanach Zjednoczonych , położona nad jeziorem Michigan , na północnych przedmieściach Chicago .
W 2016 roku miejscowość liczyła 12 417 mieszkańców .
Pierwsi Europejscy osadnicy przybyli tu w latach 20. i 30 .
XIX wieku .
Miejscowość rozplanowana została w 1854 roku , wkrótce po dotarciu tu kolei , a jej formalne założenie nastąpiło w 1869 roku .
Winnetka jest jedną z najzamożniejszych miejscowości w Stanach Zjednoczonych .
W 2015 roku średnie roczne zarobki w gospodarstwie domowym wyniosły blisko 312 tys .
dolarów , dając jej 10. pozycję pod tym względem w kraju .
Przy 671 Lincoln Avenue w tej miejscowości znajduje się dom , w którym rozgrywa się akcja filmu `` Kevin sam w domu '' a także początkowe sceny filmu `` Kevin sam w Nowym Jorku '' .
