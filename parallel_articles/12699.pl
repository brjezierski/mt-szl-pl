Winnipeg ( rzeka )
Winnipeg ( ang .
`` Winnipeg River '' , fr .
`` Rivière Winnipeg '' ) – rzeka w Kanadzie płynąca na północny zachód z Jeziora Leśnego do jeziora Winnipeg , przez prowincje Ontario i Manitobę .
Ma 235 km długości od źródła , koło miasta Kenora , do ujścia .
Zlewnia rzeki ma powierzchnię 106 500 km² , w większości znajduje się w Kanadzie , a około 29 000 km² leży na terenie północnej Minnesoty w Stanach Zjednoczonych .
Zlewnia rzeki Winnipeg rozciąga się 100 km na zachód od Jeziora Górnego .
Została ona przyznana w 1670 roku Kompanii Zatoki Hudsona jako jej najbardziej wysunięte na południowy zachód tereny .
Nazwa rzeki oznacza `` mętna woda '' w języku kri .
Szlak rzeczny Winnipeg był wykorzystywany przez tubylców tysiące lat przed kontaktem z Europejczykami .
Francuscy i angielscy kolonizatorzy po dotarciu do centralnej Kanady również zaczęli korzystać z rzeki w celu transportu futer i skór wykorzystywanych w kuśnierstwie .
Jest to jedyny spławny szlak wodny pomiędzy południową Manitobą a Ontario .
Jednym z pierwszych odkrywców , który założył forty w celu handlu skórami był Pierre Gaultier de Varennes .
Forty powstały w pobliżu obozów Indian Kanadyjskich , usytuowanych wzdłuż rzeki .
Główne dopływy rzeki Winnipeg : Black Sturgeon , English , Whitemouth , MacFarlane oraz Whiteshell .
