Wino
Wino – napój alkoholowy uzyskiwany w wyniku fermentacji moszczu winogronowego .
Istnieje wiele rodzajów win , co związane jest z mnogością odmian winorośli , oddziaływaniem środowiska na ich wzrost i różnymi technikami winifikacji ( przekształcania winogron w wino ) .
Można wyróżnić wina białe , różowe i czerwone .
Ze względu na zawartość cukru można je podzielić na wytrawne , półwytrawne , półsłodkie i słodkie .
Mogą to być wina musujące lub niemusujące .
Nauką zajmującą się produkcją wina jest enologia .
Wino składa się z wody ( 75–90 % ) , etanolu , glicerolu , polisacharydów oraz różnych kwasów i związków fenolowych .
Poza tym w winie znajdują się sole mineralne i witaminy .
Łącznie zawiera ponad tysiąc substancji , nie wszystkie są dobrze poznane .
Nie zawsze wyraz `` wino '' odnosi się do fermentowanego napoju pozyskanego z winogron , owoców winorośli właściwej .
Istnieje wiele rozmaitych win wyprodukowanych z innych owoców , także tropikalnych .
Czasami termin `` wino '' stosuje się również w odniesieniu do napojów sporządzonych z surowców zawierających skrobię , których sposób produkcji bardziej przypomina produkcję piwa niż wina .
Jest to przykładowo wino ryżowe czy barley wine ( wino jęczmienne ) , jeden ze stylów piwa .
Jednakże wyraz `` wino '' użyty bez doprecyzowania , z jakich owoców je wyprodukowano , odnosi się domyślnie do wina produkowanego wyłącznie z winogron .
Historia wina .
Winorośl najprawdopodobniej pochodzi z obszaru Kaukazu .
Hieroglificzne przedstawienia prasy winiarskiej z czasów I dynastii w Starożytnym Egipcie ( ok. 3000 p.n.e . )
stanowią jeden z pierwszych jednoznacznych dowodów na celową produkcję wina , innym są relacje Biblijne , z których pierwsza ( historia Noego ) może być datowana na podobny okres .
Uprawa winorośli rozprzestrzeniała się na Starożytną Grecję , Rzym , a następnie na nowe krainy , m.in .
na tereny dzisiejszych : Hiszpanii , Francji i Niemiec .
Wino używano zarówno w celach religijnych ( wino mszalne ) , jak i niereligijnych .
Winiarstwo rozwijało się intensywnie do połowy XIX w. aż filoksera winiec masowo zaatakowała winorośle .
Winnice trzeba było odtwarzać na odpornych podkładkach amerykańskich .
Do Polski winorośl dotarła najprawdopodobniej wraz z chrześcijaństwem , a wino produkowane było początkowo przez zakonników znających techniki winiarskie .
Okres największego rozprzestrzenienia się upraw winorośli w Polsce przypada na wiek XIV w. W XVI–XVII w. winiarstwo polskie coraz bardziej podupadało ze względu na wojny , niekorzystnie zmieniający się klimat i import win zagranicznych , zwłaszcza węgierskich .
Ostateczny upadek polskiego winiarstwa miał miejsce w XX w. , do czego przyczynił się socjalistyczny ustrój ówczesnej Polski .
