Winona ( Minnesota )
Winona – miasto w Stanach Zjednoczonych , w stanie Minnesota , siedziba hrabstwa Winona .
W 2010 roku liczyło 27 592 mieszkańców .
Miasto położone jest w dolinie Hiawatha Valley , nad rzeką Missisipi .
Pierwszym Europejczykiem , który dotarł w te rejony był franciszkański misjonarz Louis Hennepin , czego dokonał około 1680 roku .
Wkrótce jego śladem podążyli inni misjonarze oraz handlarze futrem .
