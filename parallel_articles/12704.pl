Winston Churchill
Winston Leonard Spencer Churchill ( ur .
30 listopada 1874 w Blenheim Palace , zm .
24 stycznia 1965 w Londynie ) – brytyjski polityk , mąż stanu , mówca , strateg , pisarz i historyk , malarz , dwukrotny premier Zjednoczonego Królestwa , laureat literackiej Nagrody Nobla , honorowy obywatel Stanów Zjednoczonych .
W 2002 w plebiscycie organizowanym przez BBC został uznany za najwybitniejszego Brytyjczyka wszech czasów .
Życiorys .
Wczesne lata .
Przyszły premier Wielkiej Brytanii wywodził się z bocznej linii arystokratycznej rodziny Spencer-Churchill , ale rodzina Winstona używała tylko nazwiska Churchill .
Jego ojcem był wpływowy polityk Partii Konserwatywnej lord Randolph Churchill , młodszy syn 7. księcia Marlborough .
Matka Winstona nazywała się Jeanette Jerome , była córką amerykańskiego finansisty Leonarda Jerome ’ a .
Podobnie jak większość dzieci z wyższych klas społecznych , Winston spędził większość dzieciństwa w szkołach z internatem .
Naukę pobierał w Harrow School , gdzie doszła do głosu jego niezależna i buntownicza natura .
Był on przez to często karany , pomimo dobrych wyników z języka angielskiego i historii .
Był również mistrzem szkoły w szermierce .
Gdy miał 7 lat , jego pierwszy nauczyciel skutecznie zniechęcił go do łaciny , każąc nauczyć się I deklinacji na pamięć .
Po dwóch latach pobytu w tej szkole , Winston był tak wykończony psychicznie , że musiał być przeniesiony do innej ( po rehabilitacji ) .
Przyszły premier Wielkiej Brytanii nie lubił gry w piłkę nożną i krykieta .
Młody Churchill był rzadko odwiedzany przez matkę , którą bardzo kochał ; często pisał do niej listy z prośbą , aby przyjechała lub skłoniła ojca do zezwolenia mu na powrót do domu .
Z ojcem łączyły Churchilla raczej chłodne relacje .
Jako dziecko nawiązał bliskie stosunki ze swoją nianią Elisabeth Anne Everest .
Kariera wojskowa .
Po ukończeniu Harrow School Churchill próbował dostać się do Royal Military Academy w Sandhurst , co udało mu się dopiero za trzecim podejściem .
Szkołę ukończył w 1895 i został podporucznikiem w 4 regimencie huzarów Królowej ( `` 4th Queen ’ s Own Hussars '' ) .
Jego regiment został następnie wysłany do brytyjskich Indii .
Niedługo po przybyciu na miejsce Churchill dotkliwie zranił się w ramię .
Ta rana dokuczała mu przez resztę życia .
Głównym zajęciem regimentu Churchilla w Indiach było granie w polo .
