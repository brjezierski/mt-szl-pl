Konurbacja złożona z Winston-Salem i oddalonych o 40 km miast Greensboro i High Point w 2004 roku liczyła 1,34 mln mieszkańców .
Miasto powstało w 1913 roku z połączenia dwóch miast : Salem , założonego w 1753 roku , i Winston , założonego w 1849 roku .
Dużą rolę w historii miasta odegrał przemysł tytoniowy .
W mieście znajduje się siedziba koncernu tytoniowego R. J. Reynolds Tobacco Company , produkującego między innymi papierosy marki `` Winston '' i `` Salem '' .
