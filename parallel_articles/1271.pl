Apiognomonia errabunda
Apiognomonia errabunda ( Roberge ex Desm . )
Höhn ) – gatunek grzybów z klasy `` Sordariomycetes '' .
Grzyb mikroskopijny , patogen wywołujący chorobę o nazwie antraknoza drzew liściastych
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Apiognomonia , Gnomoniaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1849 roku Michel Roberge i Jean Baptiste Henri Joseph Desmazières nadając mu nazwę `` Sphaeria errabunda '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1918 roku Höhnel , przenosząc go do rodzaju `` Apiognomonia '' .
