Największy obszar metropolitalny Virginia Beach–Norfolk–Newport News położony jest nad wybrzeżem , wykracza poza granice stanu i obejmuje 1,8 mln mieszkańców .
Z Wirginii pochodziło czterech spośród pierwszych pięciu prezydentów USA : George Washington , Thomas Jefferson , James Madison i James Monroe .
Określa się ich mianem dynastii wirginijskiej .
Symbolami Wirginii są ptak kardynał i kwiat derenia .
Geografia .
Wirginia znajduje się w środkowej części wschodniego wybrzeża USA .
Na północy graniczy z Wirginią Zachodnią i Marylandem oraz z Dystryktem Kolumbii , na zachodzie z Kentucky , a na południu z Tennessee i Karoliną Północną .
Granicę wschodnią wyznacza Ocean Atlantycki oraz zatoka Chesapeake .
Administracyjnie stan podzielony jest na 95 hrabstw oraz 39 miast niezależnych , niewchodzących w skład żadnego z hrabstw .
We wschodniej części stanu występują tereny nizinne , a w zachodniej wyżynne i górzyste ( Appalachy ) .
Najwyższy szczyt , Mount Rogers , wznosi się 1746 m n.p.m .
Klimat jest podzwrotnikowy morski .
Demografia .
Na podstawie spisu ludności z roku 2020 stwierdzono , że stan Wirginia liczył 8 631 393 mieszkańców , co oznaczało wzrost o 630 369 ( 7,9 % ) w porównaniu z poprzednim spisem z roku 2010 .
