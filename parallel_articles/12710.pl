Wirginia
Wirginia ( ) – stan w Stanach Zjednoczonych na wybrzeżu Oceanu Atlantyckiego .
Prawie dwie trzecie Wirginii jest zalesione , a branże związane z lasami zapewniają 17 mld dolarów rocznego przychodu dla gospodarki Wirginii ( 2 % PKB ) , oraz ponad 100 tys .
