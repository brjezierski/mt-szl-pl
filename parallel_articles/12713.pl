Wisbech
Wisbech – miasto i civil parish w Wielkiej Brytanii , w Anglii , w regionie East of England , w hrabstwie Cambridgeshire , w dystrykcie Fenland .
W 2011 civil parish liczyła 22841 mieszkańców .
W miejscowości tej przyszedł na świat w 1756 roku William Godwin , brytyjski publicysta i myśliciel , zaliczany do prekursorów anarchizmu .
W Wisbech ma swą siedzibę klub piłkarski – Wisbech Town F.C .
Wisbech jest wspomniana w Domesday Book ( 1086 ) jako `` Wisbece '' .
