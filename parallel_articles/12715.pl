Wishaw
Wishaw ( gael .
`` Camas Neachdain '' ) – miasto w Szkocji , w North Lanarkshire .
Historia .
Początki miasta sięgają XII wieku kiedy postawiono kościół pod wezwaniem świętego Michała .
Sama wieś została założona w 1794 , nazywana wtedy Cambusnethan , później przemianowana na Wishawtown .
4 września 1855 roku połączono wsie Coltness i Stewarton tworząc Wishaw .
W 1996 roku podczas epidemii pałeczka okrężnicy ( `` Escherichia coli '' ) w mieście zmarło 21 osób a zarażonych zostało około 200 .
Klimat i geografia .
Wishaw leży w bardzo zaludnionym obszarze , będący sam w sobie czwartym pod względem populacji w całej Szkocji .
Miasto leży blisko centrum Szkocji .
Klimat jest umiarkowany morski , co oznacza że zimy są łagodne ale mokre , a lato jest suche .
Śnieg nie występuje regularnie zimą , choć deszcz już tak .
