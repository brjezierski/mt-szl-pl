Wiszniew
Wiszniew ( biał .
`` Вішнева '' , `` Вішнеў '' ) – wieś ( dawniej miasteczko ) na Białorusi , w obwodzie mińskim , w rejonie wołożyńskim , siedziba sielsowietu Wiszniew ( `` Вішнеўскі сельсавет '' ) .
Siedziba parafii prawosławnej ( pw .
Świętych Kosmy i Damiana ) i rzymskokatolickiej ( pw .
Nawiedzenia Najświętszej Maryi Panny ) .
Historia .
W I Rzeczypospolitej miasteczko należało do powiatu oszmiańskiego w województwie wileńskim .
Stanowiło ono dziedzictwo rodziny Steczkiewiczów i Chreptowiczów .
13 stycznia 1593 w Wiszniewie zmarł Szymon Budny – działacz reformacji w I Rzeczypospolitej , pastor początkowo kalwiński , a następnie ariański , hebraista , biblista , tłumacz Biblii na język polski , pisarz i teolog ariański .
10 lipca 1895 urodził się tutaj Nachum Goldmann – prawnik , filozof pochodzenia żydowskiego , założyciel , prezes Światowego Kongresu Żydów , prezydent Światowej Organizacji Syjonistycznej .
W II Rzeczypospolitej stanowiło siedzibę gminy Wiszniew w powiecie wołożyńskim województwa nowogródzkiego .
12 listopada 1911 w Wiszniewie urodził się Jehoszua Rabinowic – izraelski polityk , minister .
2 sierpnia 1923 urodził się tutaj Szimon Peres – były prezydent Izraela .
W nocy z 18 na 19 lipca 1924 na miasteczko napadła bolszewicka grupa dywersyjna , która po zrabowaniu miejscowości wycofała się w stronę granicy .
W latach 1941–1942 funkcjonowało tu getto żydowskie , którego znaczna część ludności ( ok. 2 tys .
osób ) została wymordowana w lipcu 1942 .
Tragedii tej poświęcono kamień pamiątkowy , którego inskrypcja głosi jednak jedynie o wymordowaniu przez hitlerowców „ obywateli radzieckich ” , bez podania ich narodowości .
