Apiognomonia erythrostoma ( Pers . )
Höhn .
– gatunek grzybów z klasy `` Sordariomycetes '' .
Grzyb mikroskopijny , patogen , u niektórych roślin uprawnych wywołujący chorobę zasychanie liści czereśni .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Apiognomonia , Gnomoniaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1800 roku Christiaan Hendrik Persoon nadając mu nazwę `` Sphaeria erythrostom '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1918 roku Franz von Höhnel , przenosząc go do rodzaju `` Apiognomonia '' .
