Witebsk
Witebsk ( biał .
Віцебск , `` Wiciebsk '' ; ros .
Витебск , `` Witiebsk '' ) – miasto położone w północno-wschodniej części Białorusi , nad rzeką Dźwiną .
Liczy 364,8 tys .
mieszkańców ( 2020 ) .
Witebsk jest trzecim pod względem liczby ludności miastem białoruskim .
Miasto należało do Wielkiego Księstwa Litewskiego oraz do I Rzeczypospolitej .
Do I rozbioru było miastem królewskim i stolicą powiatu witebskiego oraz województwa witebskiego .
Miejsce obrad sejmików ziemskich województwa witebskiego od XVI wieku do pierwszej połowy XVIII wieku .
Stanowił ważną twierdzę Bramy Smoleńskiej .
Historia .
Witebsk wzmiankowany był po raz pierwszy w 1021 , przez długi czas pozostawał ośrodkiem handlowym przy szlaku znad Morza Bałtyckiego do Konstantynopola .
Od końca X wieku wchodził w skład Księstwa połockiego .
