Witoldowo ( województwo wielkopolskie )
Witoldowo – osada w Polsce położona w województwie wielkopolskim , w powiecie gostyńskim , w gminie Gostyń .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość należała do wsi mniejszych w ówczesnym pruskim powiecie Kröben ( krobskim ) w rejencji poznańskiej .
Folwark Witoldowo należał do okręgu gostyńskiego tego powiatu i stanowił część majątku Gola , którego właścicielem był wówczas ( 1846 ) Gustaw Potworowski .
Według spisu urzędowego z 1837 roku folwark liczył 92 mieszkańców , którzy zamieszkiwali 6 dymów ( domostw ) .
W latach 1975–1998 miejscowość należała administracyjnie do województwa leszczyńskiego .
