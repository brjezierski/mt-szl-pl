Witoszewo
Witoszewo ( niem .
`` Kunzendorf '' ) – wieś w Polsce położona na Pojezierzu Iławskim w województwie warmińsko-mazurskim , w powiecie iławskim , w gminie Zalewo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
W latach 1945–1946 miejscowość nosiła nazwę `` Kończewo ''
Wieś wzmiankowana w dokumentach z roku 1324 , jako wieś czynszowa na 40 włókach , pod nazwą `` Conczendorf '' .
W roku 1782 we wsi odnotowano 10 domów ( dymów ) , natomiast w 1858 w 37 gospodarstwach domowych było 409 mieszkańców .
W latach 1937–1939 było 205 mieszkańców .
W roku 1973 wieś należała do powiatu morąskiego , gmina i poczta Zalewo .
