Apiognomonia veneta
Apiognomonia veneta ( Sacc .
& amp ; Speg . )
Höhn .
– gatunek grzybów z klasy `` Sordariomycetes '' .
Grzyb mikroskopijny , patogen rozwijający się na platanach i wywołujący u nich chorobę o nazwie antraknoza platana .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Apiognomonia , Gnomoniaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1878 roku Pier Andrea Saccardo i C.L .
Spegazzini nadając mu nazwę `` Laestadia veneta '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1920 roku Franz von Höhnel , przenosząc go do rodzaju `` Apiognomonia '' .
