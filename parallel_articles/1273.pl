Anamorfa : `` Gloeosporidina platani '' Butin & amp ; Kehr 1998 .
W niektórych opracowaniach `` Apiognomonia veneta '' traktowany był jako synonim `` Apiognomonia errabunda '' .
Po zbadaniu morfologii patogenów i sekwencji ich DNA stwierdzono , że są to odrębne gatunki .
