Więcmierzyce
Więcmierzyce , niem .
Winzenberg – wieś w Polsce położona w województwie opolskim , w powiecie brzeskim , w gminie Grodków .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Przysiółkiem wsi jest Podlesie .
Historia .
W 1282 r. istniał tu zamek zamieszkały przez rycerza Alberta von Vincemericz .
W XIV wieku zamek był określany jako castrum Winthmeritz .
W 1432 r. zamek został zdobyty i zniszczony przez husytów .
W XIV wieku właścicielami miejscowości była rodzina Stosche , a następnie do XVI wieku rodzina Pogarell .
Po nich właścicielem była rodzina von Rothkirch , a w XVII wieku rodzina von Vogt .
W XVII wieku zbudowano we wsi kościół .
W 1644 roku majątek odziedziczyła Marianne von Seydlitz .
Od 1782 roku dobra więcmierzyckie stały się własnością barona von Sierstorpff , który posiadał już sąsiednie Kopice .
Od roku 1859 aż do 1945 roku właścicielami majątku była rodzina von Schaffgotsch .
Po wojnie w zespole folwarcznym powstał PGR , a w latach 90. założenie przeszło na własność ANR .
