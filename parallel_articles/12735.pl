Wierzbiec
Wierzbiec ( , ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik , w dolinie rzeki Prudnik .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Obniżenia Prudnickiego , będącego częścią Niziny Śląskiej .
Przepływa przez nią rzeka Prudnik .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 219 osób .
