Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 6 km od granicy z Czechami , w zachodniej części Obniżenia Prudnickiego , tuż przy granicy gminy Prudnik z gminą Głuchołazy .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Przez granice administracyjne wsi przepływa rzeka Prudnik .
Leży na wysokości 275–320 m n.p.m .
