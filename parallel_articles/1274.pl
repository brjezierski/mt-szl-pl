Apiosporina morbosa ( Schwein . )
Arx – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju śliwa ( `` Prunus '' ) .
Wywołuje u nich chorobę o nazwie czarna guzowatość śliwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Apiosporina , Venturiaceae , Venturiales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1882 r. Lewis David von Schweinitz nadając mu nazwę `` Sphaeria morbosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Josef Adolph von Arx w 1954 r .
Synonimy :
Charakterystyka .
Na porażonych gałązkach , gałęziach lub pniach drzew tworzą się wydłużone narośla o długości od 3,5 do 14 cm i grubości od 1 do 2,5 cm .
