Wiśniew-Kolonia
Wiśniew-Kolonia – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Leży nad rzeką Muchawką .
Przez wieś przebiega droga krajowa nr 63 Sławatycze – Węgorzewo .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Jezusowego w Wiśniewie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
