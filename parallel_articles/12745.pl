Wólka Wiśniewska
Wólka Wiśniewska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Leży nad Muchawką dopływem Liwca .
2 km od wsi przebiega droga krajowa nr 63 Węgorzewo – Sławatycze .
Natomiast przez samą wieś przebiega droga gminna Mościbrody – Rakowiec .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Jezusowego w Wiśniewie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W miejscowości działa założona w 1963 jednostka ochotniczej straży pożarnej .
Jest to jednostka typu M , czyli nie posiadająca samochodu , a jedynie motopompę wożoną na pojeździe zastępczym .
