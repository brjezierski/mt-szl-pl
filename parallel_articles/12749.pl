Wiatr
Wiatr – ruch powietrza względem powierzchni Ziemi , zwłaszcza jego pozioma składowa .
Wiatr wywołany jest przez różnicę ciśnień oraz różnice w ukształtowaniu powierzchni .
Termin wiatr jest używany w meteorologii prawie wyłącznie na określenie horyzontalnej składowej wiatru .
Istnieje jednak składowa pionowa wiatru i wtedy jest tak nazywana .
Wiatr może wiać z obszarów wyższego ciśnienia do obszarów niższego ciśnienia , ale w średnich szerokościach geograficznych , ze względu na siłę Coriolisa , wiatr wieje zazwyczaj równolegle do linii takiego samego ciśnienia ( wiatr geostroficzny ) .
Wiatr jest jednym ze składników pogody , w tym celu podaje się `` prędkość wiatru '' ( w m/s , km/h lub w węzłach ) i `` kierunek '' , z którego wieje .
Należy zwrócić uwagę , że meteorolodzy pod nazwą ( przykładowo ) `` wiatr zachodni '' rozumieją wiatr wiejący z zachodu , podczas gdy `` zachodni prąd oceaniczny '' to prąd płynący na zachód .
Wykorzystanie .
Wiatr może być wykorzystywany do produkcji energii za pomocą turbin wiatrowych oraz do napędzania statków żaglowych .
Pomiar wiatru .
Do pomiarów wiatru służy anemometr ( wiatromierz ) .
Wiatr można też mierzyć za pomocą technik satelitarnych ( teledetekcji ) za pomocą skaterometrów wykorzystujących zjawisko fal kapilarnych na wodzie ( refleks słońca ) , za pomocą teledetekcyjnych metod akustycznych sodar , za pomocą obserwacji poruszających się chmur , za pomocą radaru , za pomocą sond meteorologicznych , i innych technik .
Nazwy lokalne wiatrów .
Ze względu na zmiany temperatury , jakie przynosi ze sobą wiatr , wyróżnia się wiatry ciepłe i zimne .
Lokalne wiatry związane są ze zjawiskiem bryzy morskiej , wiatru górsko-dolinnego , wiatr katabatyczny i innych typów .
Na całym świecie wyróżnia się około 2000 nazw wiatrów lokalnych .
