Wiaźma ( stacja kolejowa )
Wiaźma ( ) – węzłowa stacja kolejowa w miejscowości Wiaźma , w rejonie wiaziemskim , w obwodzie smoleńskim , w Rosji .
Węzeł linii Moskwa - Mińsk - Brześć z liniami do Rżewa , Riażska i Briańska .
Stacja powstała w XIX w. na drodze żelaznej moskiewsko-brzeskiej pomiędzy przystankami Komiagino a Griediakino .
Jeszcze w czasach carskich powstał tu węzeł kolejowy po wybudowaniu linii kolei riażsko-wiaziemskiej , rżewsko-wiaziemskiej i nowotorżskiej .
