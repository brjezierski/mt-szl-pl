Wiechowicze
Wiechowicze ( do 31 grudnia 2014 `` Wichowicze '' ) – wieś w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Konstantynów .
Siedziba sołectwa Wiechowicze , które w 2006 zamieszkiwały 84 osoby .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Elżbiety Węgierskiej w Konstantynowie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
