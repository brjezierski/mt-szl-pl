Wiedeń
Wiedeń ( niem .
`` Wien '' ) – stolica i największe miasto w Austrii położone w północno-wschodniej części kraju , nad Dunajem oraz miasto statutarne tworzące jednocześnie odrębny kraj związkowy .
Wiedeń jest ośrodkiem administracyjnym , przemysłowym , handlowo-usługowym , akademickim , turystycznym i kulturalnym o znaczeniu międzynarodowym .
Jest siedzibą austriackich urzędów centralnych ( w tym parlamentu , prezydenta i rządu ) , związków wyznaniowych działających na terenie Austrii , a także licznych przedsiębiorstw , stowarzyszeń , organizacji i uczelni .
Vienna International Centre jest jednym z czterech kompleksów , w których ulokowane są instytucje Organizacji Narodów Zjednoczonych .
Swoje siedziby w Wiedniu mają również , MAEA , OBWE oraz OPEC .
W skali kraju miasto stanowi najważniejszy węzeł drogowy i kolejowy oraz lotniczy .
Został założony około 500 p.n.e .
jako osada celtycka .
W 15 p.n.e .
stał się rzymskim posterunkiem granicznym .
Prawa miejskie uzyskał w roku 1221 , stając się jednym z największych i najważniejszych miast Świętego Cesarstwa Rzymskiego ( Starej Rzeszy ) , a po jego upadku – stolicą Cesarstwa Austrii , a następnie Austro-Węgier .
W roku 1918 stał się stolicą Republiki Austrii .
Historyczne centrum miasta , pełne zabytków ze wszystkich epok historycznych z przewagą XIX-wiecznego historyzmu i secesji przełomu XIX i XX wieku , zostało w 2001 roku wpisane na listę światowego dziedzictwa UNESCO .
Ze względu na bogactwo atrakcji należy do najchętniej odwiedzanych miast Europy .
Według danych z 1 stycznia 2020 roku liczba ludności miasta wynosi 1 911 191 ( 1. miejsce w Austrii ) , natomiast powierzchnia – 414,87 km² ( również 1. miejsce w Austrii ) .
Wiedeń oficjalnie podzielony jest na 23 dzielnice .
Obszar metropolitalny Wiednia liczy , według danych z 2018 roku 2 838 558 mieszkańców .
Wiedeń , według raportu amerykańskiego Mercera , oceniającego 221 najważniejszych metropolii , jest miastem o najwyższej jakości życia na świecie .
W rankingu oceniano m.in .
sytuację ekonomiczną , stan środowiska naturalnego , wskaźnik przestępczości , dostęp do oświaty i kultury oraz jakość służby zdrowia .
Austriacka stolica uzyskała 108,6 pkt ( pozycję miasta ustalano w odniesieniu do Nowego Jorku , któremu przypisano ranking 100,0 ) zaś 2. i 3. miejsca zajęły szwajcarskie Zurych i Genewa .
Historia .
Założony około 500 p.n.e. , Wiedeń był pierwotnie celtycką osadą .
W 15 p.n.e .
miasto pełniło funkcję rzymskiego posterunku granicznego ( `` Vindobona '' ) strzegącego Imperium Rzymskiego przed najazdami germańskich plemion z północy .
W 1221 roku Wiedeń uzyskał prawa miejskie .
We wczesnym średniowieczu miasto rządzone było przez dynastię Babenbergów , a po jej wygaśnięciu , od 1251 do 1276 , znajdowało się pod panowaniem czeskiego króla Przemysła Ottokara II , za którego rządów nastąpił okres rozkwitu gospodarczego , mimo pożarów w 1258 i 1262 .
Wówczas , w 1261 , w Wiedniu został zawarty pokój między Czechami a Węgrami .
W 1276 roku rządy objęła dynastia Habsburgów .
Za ich rządów Wiedeń stał się stolicą Świętego Cesarstwa Rzymskiego , siedzibą biskupstwa , centrum naukowym i kulturalnym .
W czasie panowania Rudolfa IV Założyciela ( 1358–1365 ) rozpoczęła się budowa katedry św .
Szczepana , utworzono uniwersytet .
W 1485 roku Wiedeń został zdobyty przez króla węgierskiego Macieja Korwina .
Pod jego panowaniem pełnił przez pięć lat funkcję stolicy Królestwa Węgier .
Jako znaczący ośrodek handlowy Europy Środkowej w XVI wieku Wiedeń był miastem bardzo zróżnicowanym narodowościowo .
Dramatopisarz Wolfgang Schmeltzl w 1548 porównał miasto do wieży Babel , i wśród usłyszanych w Wiedniu języków wymienił m.in .
grecki , łacinę , francuski , turecki , hiszpański , czeski , łużycki , włoski , węgierski , niderlandzki , chorwacki , serbski , polski i aramejski .
Od połowy XVI wieku zaczęto rozbudowywać fortyfikacje miejskie z powodu ciągłego zagrożenia ze strony tureckiej .
W XVI i XVII wieku , dwukrotnie zdołano odeprzeć ataki Turków , również dzięki pomocy ze strony Polski ( odsiecz wiedeńska ) .
W XVI wieku w Wiedniu rozpoczął się okres reformacji .
W 1679 roku miasto dotknęła epidemia zarazy , która pochłonęła życie około 70 tys .
mieszkańców .
Po serii klęsk , które nawiedziły miasto , w XVIII wieku rozpoczął się tzw .
„ złoty okres ” w historii miasta .
Znacznie rozwinął się handel , przemysł , kultura i sztuka .
W latach 1814–1815 w Wiedniu obradował kongres wiedeński , na którym ustalono nowy ład w Europie .
W okresie Wiosny Ludów , w 1848 roku doszło do licznych manifestacji społecznych w mieście , mieszkańcy domagali wprowadzenia reform w kraju .
Wkrótce po tym władzę objął Franciszek Józef I , który doprowadził do utworzenia monarchii austro-węgierskiej w 1867 roku .
W owym czasie zlikwidowano mury obronne , by na ich miejscu zbudować reprezentacyjną ulicę miasta , Ringstraße .
Wówczas też powstało wiele budowli , które do dziś pełnią swą reprezentacyjną funkcję .
Jako stolica imperium , Wiedeń już w latach dwudziestych ( konkretnie w 1826 r. ) uzyskał połączenie żeglugowe z Budapesztem , obsługiwane przez Towarzystwo Żeglugi Parowej Dunaju .
Na początku lat pięćdziesiątych podróż statkiem rzecznym zajmowała czternaście godzin .
