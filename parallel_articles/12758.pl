Srocko Wielkie
Srocko Wielkie – wieś w Polsce położona w województwie wielkopolskim , w powiecie kościańskim , w gminie Czempiń .
Wieś szlachecka Sroczko maior położona była w 1581 roku w powiecie kościańskim województwa poznańskiego .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako Srocko Wielkie należała do wsi większych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Srocko Wielkie należało do okręgu czempińskiego tego powiatu i stanowiło – wraz ze wsią Bieczyn – odrębny majątek , którego właścicielem był wówczas ( 1846 ) Kwilecki .
Według spisu urzędowego z 1837 roku Srocko Wielkie liczyło 256 mieszkańców , którzy zamieszkiwali 25 dymów ( domostw ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
