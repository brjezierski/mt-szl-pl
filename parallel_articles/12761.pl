Wielkie Tyrnowo
Wielkie Tyrnowo ( – Weliko Tyrnowo ) – miasto położone w północno-centralnej Bułgarii nad rzeką Jantrą , u północnych podnóży Starej Płaniny , około 240 km na północny wschód od Sofii .
Jest jednym z najstarszych miast w Bułgarii , historycznie trzecią stolicą państwa , symbolem bułgarskiej niepodległości i państwowości .
Według Ujednoliconego Systemu Ewidencji Ludności oraz Usług Administracyjnych dla Ludności , 15 marca 2016 roku miasto liczyło 71 168 mieszkańców .
Położone jest na kilku wzgórzach , oddzielonych od siebie silnie meandrującą tu Jantrą , z których najważniejsze to Sweta Gora , Carewec i Trapezica .
Lokalizacja miasta wymusiła szczególną , wyjątkowo ciasną zabudowę , sprawiając wrażenie , iż domy stoją jeden na drugim .
Obecnie Wielkie Tyrnowo jest stolicą obwodu Wielkie Tyrnowo i gminy Wielkie Tyrnowo .
Znajduje się tutaj jedna z najważniejszych bułgarskich uczelni wyższych : Uniwersytet Wielkotyrnowski im .
