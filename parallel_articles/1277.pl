Apodaca
Apodaca – miasto w Meksyku w stanie Nuevo León , leży w północno-wschodniej części obszaru metropolitalnego Monterrey .
Jedno z najszybciej rozwijających się miast regionu , ważny ośrodek przemysłu elektronicznego .
Znajdują się tu dwa duże lotniska : Port lotniczy Monterey-Mariano Escobedo oraz Aeropuerto Internacional del Norte .
Swoją siedzibę mają tu meksykańskie linie lotnicze VivaAerobus .
