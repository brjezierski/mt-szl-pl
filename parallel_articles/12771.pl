Stożek Wielki ( Góry Kamienne )
Stożek Wielki ( 841 m n.p.m . )
– wzniesienie w Sudetach Środkowych , w Górach Kamiennych , na północno-zachodnim krańcu Gór Suchych , w północnej części pasma Stożka .
Położenie .
Położony w północno-środkowej części Gór Kamiennych , na pograniczu z Górami Wałbrzyskimi , na północnym zachodzie od miejscowości Sokołowsko .
Od strony wschodniej oddzielony Kotliną Sokołowską od masywu Bukowca , a od zachodniej strony głęboka dolina przełomu Ścinawki oddziela wzniesienie od Masywu Dzikowca i Lesistej Wielkiej .
Jest to jedno z najbardziej charakterystycznych wzniesień powulkanicznych , o formie poosuwiskowej w kształcie stożka , o ostro zakończonej części szczytowej i stromych zboczach , z wyraźnie zaznaczonymi wierzchołkiem .
Wzniesienie zbudowane z permskich skał wylewnych , należących do północnego skrzydła niecki śródsudeckiej .
Na północno-zachodnim zboczu , na wysokości około 780 m n.p.m. , charakterystyczna grupa powulkanicznych skałek .
Wzniesienie od poziomu około 760 m po wschodniej stronie i od 550 m n.p.m. , po zachodniej stronie porośnięte jest w całości lasem regla dolnego , górną część wzniesienia zajmuje rozrzedzony las z przewagą drzewostanu liściastego .
Na wschodnim zboczu góry poniżej granicy lasu rozciąga się obszerna górska łąka z ciekawą roślinnością , a niżej położone są pola uprawne .
Szczyt wziął nazwę od swojego charakterystycznego stożkowego kształtu .
Nazwę `` Stożek Wielki '' wprowadzono urzędowo w 1949 roku , zastępując poprzednią niemiecką nazwę `` Groß Storch Berg '' .
Położony na terenie Parku Krajobrazowego Sudetów Wałbrzyskich .
