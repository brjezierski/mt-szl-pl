Wielka Brytania ( wyspa )
Wielka Brytania ( , , ) – wyspa na północy Oceanu Atlantyckiego , położona na północny zachód od kontynentalnej Europy .
W całości wchodzi w skład państwa Wielka Brytania , stanowiąc większość jego powierzchni ( Anglia , Szkocja i Walia ) .
Jest największą wyspą w archipelagu Wysp Brytyjskich i zarazem największą wyspą Europy , jej powierzchnia wynosi 209 331,1 km² , co czyni ją także 9. wyspą pod względem powierzchni na świecie .
Od kontynentalnej Europy oddziela ją kanał La Manche ( ang .
`` English Channel '' , „ Kanał Angielski ” ) , który w najwęższym miejscu ( Cieśnina Kaletańska ) ma szerokość 32 km .
Od położonej na zachód Irlandii oddziela Wielką Brytanię Morze Irlandzkie oraz Kanał Świętego Jerzego i Kanał Północny .
