Jedna z najbardziej dziewiczych i bezludnych ( bez stałego osadnictwa ) grup górskich na Słowacji , charakteryzująca się długimi , halnymi , widokowymi grzbietami , głębokimi dolinami , licznymi formacjami skalnymi oraz rozległymi kompleksami leśnymi .
Prawie w całości objęta ochroną w ramach Parku Narodowego Wielka Fatra , a przy tym dobrze zagospodarowana turystycznie ( liczne szlaki turystyczne ) .
