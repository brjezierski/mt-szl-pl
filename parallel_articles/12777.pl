Wielka Sowa
Wielka Sowa ( , po 1945 przez krótki okres pol .
`` Góra Sowia '' , 1015 m n.p.m . )
– najwyższy szczyt Gór Sowich w Sudetach Środkowych , w województwie dolnośląskim ; należy do Korony Gór Polski .
Położenie i opis .
Góra leży w dorzeczu Odry , a dokładniej na granicy między dorzeczami jej dopływów – Bystrzycy i Nysy Kłodzkiej .
Góra zbudowana jest ze skał metamorficznych z prekambru .
Zbocza Wielkiej Sowy są znakomitymi terenami narciarskimi .
Wokół Wielkiej Sowy dostępnych jest mnóstwo szlaków turystycznych , tras rowerowych oraz sowiogórskich tras do narciarstwa biegowego .
Znajdujący się tu wyciąg orczykowy i trasy zjazdowe są nieczynne od 2011 roku .
Dzierżawca terenu stara się o rozbudowę i uruchomienie stacji narciarskiej z prawdziwego zdarzenia .
Wieża widokowa .
Na szczycie Wielkiej Sowy znajduje się betonowa wieża widokowa wybudowana w roku 1906 .
Z racji położenia w środkowej części Sudetów jest wspaniałym punktem widokowym .
W drugiej połowie XIX wieku zainicjowano budowę pierwszej drewnianej wieży widokowej na wierzchołku góry .
Jej uroczyste oddanie do użytku odbyło się w lipcu 1885 roku .
Konstrukcja ta znajdowała się około 30 metrów na południe od miejsca , gdzie obecnie stoi żelbetowa wieża .
Drewniana konstrukcja przetrwała jednak tylko do 1904 roku .
Jeszcze w tym samym roku powstał plan wzniesienia nowej , drewnianej konstrukcji .
Ostatecznie stało się inaczej .
Prezes dzierżoniowskiego Towarzystwa Sowiogórskiego Richard Tamm przeforsował ideę budowy trwałej kamienno-żelbetowej wieży .
Prace przy jej wznoszeniu rozpoczęły się w lipcu 1905 roku , a uroczyste otwarcie , które odbyło się 24 maja 1906 roku , zgromadziło tłumy ludzi .
Wieży nadano wówczas imię Ottona von Bismarcka ( `` Bismarckturm '' ) .
Miała ona 25 m wysokości , średnicę 8 m u podstawy i 4 m u góry , a we wnętrzu , oświetlonym 3 oknami z kolorowymi witrażami przedstawiającymi sceny z życia Bismarcka , można było podziwiać popiersie kanclerza autorstwa .
Na tarasie widokowym podziwiano panoramy od Śnieżnika po Śnieżkę oraz od Wzgórz Trzebnickich po Broumovské stěny .
Tuż po II wojnie światowej wieży nadano imię Władysława Sikorskiego , a 27 września 1981 polskiego turysty i krajoznawcy Mieczysława Orłowicza , lecz nazwy te nie przyjęły się .
Wieża była jedną z atrakcji Gór Sowich , jednak nie remontowana popadała w ruinę .
Już w połowie lat 60 .
XX w. żelazne schody wewnątrz konstrukcji groziły zawaleniem , a z czasem ze względów bezpieczeństwa wieża została zamknięta .
Stan taki trwał do 2005 r .
Dopiero gdy zarządcą wieży i otaczającego ją terenu została Gmina Pieszyce udało się wykonać generalny remont wieży i w dniu 24 maja 2006 r. na nowo udostępnić obiekt turystom .
Wizerunek wieży stał się symbolem Pieszyc , znajduje się w herbie tego miasta .
Ponowny remont wieży rozpoczął się w sierpniu 2021 r. Planowane zamknięcie wieży dla turystów potrwa do 2023 r .
Główne szlaki na szczyt prowadzą z Przełęczy Jugowskiej , Sokolej i Walimskiej , jednak istnieje wiele alternatywnych szlaków .
Masyw położony jest w Parku Krajobrazowym Gór Sowich .
