Apollo 8
Apollo 8 – druga załogowa misja kierowana przez amerykańską agencję kosmiczną NASA w ramach programu Apollo .
Apollo 8 było pierwszą misją , która wyniosła ludzi poza orbitę okołoziemską .
Pierwsza misja w historii , podczas której człowiek okrążył Księżyc .
Misja Apollo 8 trwała 6 dni i 3 godziny : przeciwną stronę Księżyca osiągnięto po ponad 69 godzinach lotu , a w ciągu 20 godzin lotu wokół Księżyca , moduł załogowy dziesięciokrotnie okrążył satelitę Ziemi .
Jej celem było zebranie informacji o możliwych miejscach lądowania .
Z modułu załogowego tej misji , wykonano pierwsze w historii fotografie całej Ziemi .
Załoga .
W związku ze zmianą charakteru misji , NASA zmieniła również załogę .
Astronauci wyznaczeni do misji Apollo 8 zostali przesunięci do misji Apollo 9 .
Misja .
Astronauci Apollo 8 byli pierwszymi ludźmi , którzy opuścili niską orbitę okołoziemską i odwiedzili inne ciało niebieskie .
Początkowo zakładano , że następna misja po Apollo 7 będzie testem na orbicie okołoziemskiej , tak jak kolejna , Apollo 9 .
Plany zmieniono z obawy przed szybkimi postępami radzieckiego programu Zond , mającego na celu wysłanie wokół Księżyca pojazdu Sojuz wyniesionego przez rakietę Proton z jednym lub dwoma kosmonautami na pokładzie .
Ta informacja o radzieckich zamiarach , która dotarła do NASA latem 1968 roku , spowodowała wyznaczenie nowego celu misji Apollo 8 .
