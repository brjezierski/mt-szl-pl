Krzyż Wielkopolski ( gmina )
Krzyż Wielkopolski ( do końca 1991 `` gmina Krzyż '' ) – gmina miejsko-wiejska w województwie wielkopolskim , w powiecie czarnkowsko-trzcianeckim .
W latach 1975–1998 gmina położona była w województwie pilskim .
Siedziba gminy to Krzyż Wielkopolski .
Według danych z 30 czerwca 2004 gminę zamieszkiwało 8820 osób .
