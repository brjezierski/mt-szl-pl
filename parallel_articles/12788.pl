Wielkopolska
Wielkopolska ( ) – kraina historyczna w środkowej i zachodniej Polsce , na Pojezierzu Wielkopolskim i Nizinie Południowowielkopolskiej , w dorzeczu środkowej i dolnej Warty ; dzielnica historyczna Polski .
Historycznymi stolicami Wielkopolski są Poznań i Kalisz .
Wielkopolska nie posiada herbu .
Pierwotny herb Wielkopolski , nadany przez Kazimierza III Wielkiego , jest współcześnie herbem województwa kaliskiego .
Patronką Wielkopolski jest księżna kaliska bł .
Jolenta Helena z dynastii Arpadów , żona Bolesława Pobożnego – księcia Wielkopolski , którego bratankiem był król Przemysł II .
Władcy Wielkopolski ( książęta poznańscy i książęta kaliscy ) pochowani zostali w Złotej Kaplicy i w kolegiacie św .
Pawła Apostoła w Kaliszu .
Nazwa .
Ekspansja państwa Polan w X wieku sprawiła , że pierwotne terytorium plemienne Polan , początkowo określane jako `` Polonia '' , później zaczęto nazywać `` Polonia Maior '' ( w odróżnieniu od `` Polonia Minor '' – Małopolski ) .
Pierwszym , który w zachowanych dokumentach użył nazwy „ Wielkopolska ” ( `` Polonia Maior '' ) , był Maciej z Lubinia , benedyktyn z klasztoru w Lubiniu i notariusz księcia kaliskiego Boleslawa Pobożnego , który wymienił nazwę „ Wielkopolska ” w dokumencie z 15 czerwca 1257 .
