Apple ( przedsiębiorstwo )
Apple Inc. ( dawniej Apple Computer Inc. ) – amerykańskie przedsiębiorstwo informatyczne z siedzibą w Cupertino w Kalifornii .
Zajmuje się projektowaniem i produkcją komputerów osobistych i mobilnych oraz oprogramowania i serwisów internetowych .
Założona 1 kwietnia 1976 przez Steve ’ a Wozniaka – projektanta , Steve ’ a Jobsa i przedsiębiorcę Ronalda Wayne ’ a , aby projektować i sprzedawać kompletne , zmontowane zestawy komputerów osobistych .
Od stycznia 1977 działała formalnie jako spółka Apple Computer Inc. , a od stycznia 2007 nazwę zmieniono na Apple Inc .
Spółka publiczna notowana od 1980 roku na Nowojorskiej Giełdzie Technologicznej ( NASDAQ ) .
Od stycznia 2010 przedsiębiorstwo działa poprzez 284 własnych sklepów detalicznych w dziesięciu krajach oraz za pośrednictwem sklepu internetowego sprzedającego sprzęt i oprogramowanie .
Ze względu na design i specyficzne kampanie reklamowe , marka Apple Inc. wyróżnia się w segmencie rynku , w którym działa .
Według badań magazynu Forbes Apple była najsilniejszą marką na świecie w 2012 .
Historia .
1976–1984 : Początki .
Pierwszym produktem firmy był , zaprojektowany przez Steve ’ a Wozniaka w 1976 Apple I. Był to drugi komputer na rynku sprzedawany jako złożony ( pierwszym był Altair 8800 z 1975 ) .
