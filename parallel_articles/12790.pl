Wielgorz
Wielgorz – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mordy .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Jezusowego w Krzymoszach .
W miejscowości działa założona w 1946 roku jednostka ochotniczej straży pożarnej .
Jednostka od 2001 roku jest w strukturach krajowego systemu ratowniczo-gaśniczego .
OSP posiada średni samochód ratowniczo-gaśniczny GBA 2,5/25/4,7 Iveco 4x4 .
Historia .
Według mediewisty Franciszka Piekosińskiego nazwa wsi jest odimienna i pochodzi o imienia pierwszego właściciela , którym miał być rycerz Wielgor .
Po raz pierwszy nazwa `` Wielgori `` odnotowana została w księgach kościelnych w 1471 .
Następnie pojawia się jako `` Wielgor `` w łukowskich księgach podkomorskich z 1509 i odnosi się do dóbr szlachty zagrodowej ( Wielgorskich a. Wielgórskich ) , które do 1529 rozpadły się na trzy osady : Wyelgor , Kowyesse i Baycze .
W rejestrach podatkowych z 1531 wszystkie trzy wsie notowane jako część grupy wsi Wielgor : Wielgor-Krzymosze ( `` Vyelgor Krzymosza '' ) , Wielgor-Kowiesie ( `` Vyelgor Covyessye '' ) i Wielgor-Bajce ( `` Vyelgor Bayczye '' ) .
Granicą dóbr Wielgorz na wschodzie była wówczas rzeka Liwiec i oddzielała je od litewskich wówczas dóbr Mordy .
Co najmniej od 1552 zachodnia część dóbr nosi już tylko nazwę Krzymosze ( `` Krzimosze '' ) .
Wielgor-Bajce ( a. Zajce ) i Wielgor-Kowiesie notowane są pod tymi nazwami w księgach kościelnych jeszcze w 1721 .
Potem w źródłach występuje już tylko Wielgorz ( a. Wielgórz ) .
Obecna wieś Wielgorz niewątpliwie leży na terenie dawnej wsi Wielgor-Kowiesie .
Natomiast Bajce zostały wchłonięte albo przez obecny Wielgorz albo przez Krzymosze .
