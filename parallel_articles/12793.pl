Wielkanoc ( Bodzów )
Wielkanoc ( 222 m ) – niewielkie wzniesienie w Krakowie , w Dzielnicy VIII Dębniki , pomiędzy osiedlami Kostrze i Bodzów .
W podziale fizycznogeograficznym Polski znajduje się w mezoregionie Pomost Krakowski .
Wielkanoc znajduje się po zachodniej stronie wzgórza Solnik i jest jego częścią .
Przez płytką przełęcz między Solnikiem i Wielkanocą biegnie ulica Widłakowa , Wielkanoc znajduje się po jej zachodniej stronie .
Jest całkowicie porośnięta lasem .
Zbudowana jest ze skał wapiennych pochodzących z jury późnej .
Austriacy podczas budowy Twierdzy Kraków wykuli w skałach Wielkanocy kawernę służącą do magazynowania amunicji .
Jest to Kawerna w Wielkanocy .
Wielkanoc włączona została w obszar rekreacyjny Krakowa o nazwie Uroczysko Kostrze .
