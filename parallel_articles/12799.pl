Ługi Wielkie
Ługi Wielkie – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Zbuczyn .
Wieś szlachecka położona była w drugiej połowie XVI wieku w ziemi łukowskiej województwa lubelskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Od nazwy wsi ( gniazda ) swe nazwisko wywodzi ród Ługowskich .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Stanisława i Aniołów Stróżów w Zbuczynie .
