Łąsko Wielkie
Łąsko Wielkie – wieś o rolniczym charakterze , położona w województwie kujawsko-pomorskim , w powiecie bydgoskim , w gminie Koronowo , na północny zachód od Koronowa .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według danych Urzędu Gminy Koronowo ( XII 2015 r. ) miejscowość liczyła 396 mieszkańców .
Historia .
Wieś wymieniana w dokumentach już w końcu XIII wieku ( 1288 ) , w 1358 r. nabyta przez zakon cystersów z pobliskiego Koronowa .
10 października 1410 w okolicach wsi miała miejsce zwycięska bitwa wojsk polskich z Krzyżakami .
