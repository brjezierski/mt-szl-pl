Wieleń ( stacja kolejowa )
Wieleń – jedna z dwóch stacji kolejowych w Wieleniu , w województwie wielkopolskim .
Stacja jest położona na linii kolejowej nr 203 i obsługuje ruch pasażerski i towarowy .
W roku 2017 stacja obsługiwała 100–149 pasażerów na dobę .
Budynek dworcowy jest położony na południe od torów stacyjnych i spełnia funkcję poczekalni ; kasa biletowa nie funkcjonuje .
Pasażerska część stacji przylega do dworca i składa się z dwóch jednokrawędziowych peronów wyspowych .
Ruch na stacji sterowany jest z jednej nastawni i zabezpieczany przez semafory kształtowe .
