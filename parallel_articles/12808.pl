Wientian
Wientian ( // , ) – stolica Laosu , położona nad rzeką Mekong , w środkowo-zachodniej części kraju , tuż przy granicy z Tajlandią .
Terytorium miasta obejmuje 5 dzielnic , wchodzących w skład prefektury Wientian : Chantabuly , Sikhottabong , Xaysetha , Sisattanak i Hadxaifong .
Istnieje też prowincja Wientian , która do 1989 tworzyła wraz z prefekturą Wientian jeden region administracyjny .
Toponimia .
Laotańskie słowo ວຽງ // oznacza „ miasto otoczone murami ” ; ຈັນ // – „ drewno sandałowe ” bądź „ księżyc ” .
Indyjska nazwa najstarszego znanego organizmu politycznego na terenie dzisiejszego Wientianu brzmiała `` Chandapuri '' , czyli „ Miasto Drewna Sandałowego ” .
Tak też najczęściej tłumaczy się dzisiaj znaczenie nazwy miasta .
Klimat .
Według klasyfikacji Köppena-Geigera w Wientianie panuje klimat tropikalny ( Aw ) .
Opady deszczu w zimie są o wiele mniej obfite niż w lecie .
Najcieplejszymi miesiącami są czerwiec i lipiec z temperaturą średnią 27,4 °C , a najzimniejszym styczeń 20,9 °C
