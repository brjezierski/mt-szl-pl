Morela pospolita
Morela pospolita ( `` Prunus armeniaca '' L. ) – gatunek rośliny wieloletniej należący do rodziny różowatych .
Pochodzi z Azji , rodzimy obszar jej występowania obejmuje Kirgistan , Chiny i Koreę .
Jako gatunek introdukowany rośnie dziko także w Japonii .
Jest uprawiany w wielu krajach świata , również w Polsce .
Historia uprawy i produkcja współcześnie .
Po raz pierwszy zaczęto ją uprawiać w Chinach przed ponad 4000 lat .
Stamtąd stopniowo rozpowszechniła się przy pomocy karawan kupieckich w Azji Środkowej , Iranie , Azji Mniejszej i Syrii .
