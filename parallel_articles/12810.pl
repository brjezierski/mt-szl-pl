Wieprze
Wieprze ( lit .
`` Vepriai '' ) – miasteczko na Litwie , położone w okręgu wileńskim , w rejonie wiłkomierskim , usytuowane na południowy zachód od Wiłkomierza , mające około 1000 mieszkańców ( 2005 ) .
Historia .
Wieprze zostały wzniesione na wzgórzu nieopodal jeziora Wieprze oraz na południe od rzeki Święty .
Pierwszy raz o Wieprzach ( `` Weperen '' ) wspomina się w opisie dróg przygotowanym w 1384 roku przez zakon krzyżacki .
W średniowieczu Wieprze były grodem warownym , chroniącym ziemie litewskie przed napadami z zachodu , głównie ze strony Krzyżaków .
Podczas konfliktu o władzę w Wielkim Księstwie litewskim , zjednoczone siły wielkiego księcia litewskiego Witolda Kiejstutowicza oraz Zakonu Krzyżackiego zaatakowały i zdobył zamek wieprzański w 1384 .
Gdy tylko zagrożenie znikło , zamek został przekształcony w dwór stanowiący własność kolejno wielu szlacheckich rodzin : Kieżgajłów , następnie ks .
Ogińskich , od których drogą wiana przeszła do Szemiottów , od tych zaś , również jako wiano , do Tyzenhauzów .
