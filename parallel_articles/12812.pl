Wieruszów ( stacja kolejowa )
Wieruszów – stacja kolejowa w Wieruszowie , w województwie łódzkim , w Polsce .
Stacja na linii kolejowej nr 181 z Wielunia Dąbrowy do Kępna .
Stacja znajduje się w lewobrzeżnej części Wieruszowa o historycznej nazwie Podzamcze , do 1953 była to samodzielna miejscowość .
W roku 2017 stacja obsługiwała 0–9 pasażerów na dobę .
Budynek poczekalni jest zamknięty dla podróżnych .
Połączenia bezpośrednie .
Najbliższa przystanek kolejowy na którym zatrzymują się pociągi pospieszne to oddalony o dwa kilometry Wieruszów Miasto .
