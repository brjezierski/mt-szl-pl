Wierzawice ( gromada )
Wierzawice – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Wierzawice z siedzibą GRN w Wierzawicach utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie łańcuckim w woj .
rzeszowskim na mocy uchwały nr 27/54 WRN w Rzeszowie z dnia 5 października 1954 .
W skład jednostki wszedł obszar dotychczasowej gromady Wierzawice ze zniesionej gminy Giedlarowa w tymże powiecie .
Dla gromady ustalono 22 członków gromadzkiej rady narodowej .
1 stycznia 1956 gromada weszła w skład nowo utworzonego powiatu leżajskiego w tymże województwie .
