Wierzbica ( gmina Wierzbica )
Wierzbica – wieś w Polsce położona w województwie lubelskim , w powiecie chełmskim , w gminie Wierzbica .
Leży przy drodze wojewódzkiej nr 841 .
Krótki opis .
W latach 1867–1954 miejscowość była siedzibą gminy Olchowiec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 298 mieszkańców i była dziewiątą co do wielkości miejscowością gminy Wierzbica .
We wsi znajduje się Urząd Gminy , park z muszlą koncertową , kościół , boisko klubu sportowego `` Ogniwo '' .
Wierzbica wraz z sąsiednim Busównem i Busownem-Kolonią tworzy ciągły obszar zabudowany .
Szkole Podstawowej w Wierzbicy patronuje trener reprezentacji Polski w piłce nożnej Kazimierz Górski ( 1921–2006 ) .
