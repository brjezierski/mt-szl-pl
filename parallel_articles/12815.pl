Wierzbna ( herb szlachecki )
Wierzbna , Wierzbno – polski herb szlachecki .
Opis herbu .
W polu błękitnym , dzielonym na pół złotą belką , 6 złotych lilii .
Klejnot : złota kolumna z koroną , przebita strzałą pod kątem od spodu do góry .
Labry : błękitne podbite złotem .
Według Kulikowskiego klejnotem jest złota kolumna ( bez korony ) , przeszyta srebrną strzałą w skos .
Herbowni .
Brantalski , Gronowski , Grynfar , Łaszczyński , Pawłowski , Rydzyński , Słonczyński , Wierzbna , Wierzbnowski , Wierzwiński
Lista sporządzona została na podstawie wiarygodnych źródeł , zwłaszcza klasycznych i współczesnych herbarzy .
Należy jednak zwrócić uwagę na częste zjawisko przypisywania rodom szlacheckim niewłaściwych herbów , szczególnie nasilone w czasie legitymacji szlachectwa przed zaborczymi heroldiami , co zostało następnie utrwalone w wydawanych kolejno herbarzach .
Identyczność nazwiska nie musi oznaczać przynależności do danego rodu herbowego .
Przynależność taką mogą bezspornie ustalić wyłącznie badania genealogiczne .
