Wierzchowo ( niem .
`` Virchowsee '' ) – jezioro w woj .
zachodniopomorskim , w powiecie szczecineckim , w gminie Szczecinek , leżące na terenie Pojezierza Drawskiego .
Urozmaicona linia brzegowa , długie , piaszczyste plaże .
W jeziorze można złowić m.in .
takie ryby jak : węgorz , sieja , sielawa , leszcz , lin , płoć .
W pobliżu jeziora znajduje się użytek ekologiczny „ Wielkie Błoto ” , ośrodek wydobycia gazu ziemnego oraz wsie Wierzchowo i Stare Wierzchowo .
