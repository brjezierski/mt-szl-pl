Wierciny
Wierciny ( niem .
`` Horstenbusch '' ) – wieś w Polsce położona w województwie pomorskim , w powiecie nowodworskim , w gminie Nowy Dwór Gdański na obszarze Wielkich Żuław Malborskich nad Nogatem .
W latach 1945–1946 wieś nosiła nazwę `` Krzewiny '' .
Wieś komornictwa zewnętrznego Elbląga w XVII i XVIII wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
