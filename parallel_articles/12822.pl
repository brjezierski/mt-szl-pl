Wietnam
Wietnam , Socjalistyczna Republika Wietnamu ( wiet .
`` Việt Nam '' , `` Cộng hòa xã hội chủ nghĩa Việt Nam '' , ) − państwo w Azji Południowo-Wschodniej , położone na Półwyspie Indochińskim i graniczące z Chińską Republiką Ludową , Laosem i Kambodżą .
Ma ponad 100 mln mieszkańców i jest piętnastym co do wielkości populacji państwem świata .
Ustrój polityczny .
Konstytucja została uchwalona 15 kwietnia 1992 .
Zgromadzenie Narodowe składa się z 498 posłów ( 458 członków Komunistycznej Partii Wietnamu , 40 posłów niezależnych ) .
Powołuje rząd i prezydenta .
Według Amnesty International rządząca partia stosuje masowe represje wobec opozycji , ograniczona jest wolność słowa i zgromadzeń .
