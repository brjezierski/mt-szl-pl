Język wietnamski
Język wietnamski ( wiet .
, lub ) – narodowy i oficjalny język Wietnamu .
Jest językiem ojczystym Wietnamczyków ( ) , stanowiących ok. 87 % populacji tego kraju , oraz ok. dwóch milionów wietnamskich emigrantów .
Jako drugi język używany jest przez mniejszości narodowe Wietnamu .
Język wietnamski zawiera wiele zapożyczeń z chińskiego i pierwotnie zapisywany był za pomocą znaków chińskich .
Z punktu widzenia klasyfikacji genetycznej jest jednak przedstawicielem rodziny języków austroazjatyckich , podczas gdy chiński należy do rodziny sinotybetańskiej .
Wietnamski ma największą liczbę użytkowników spośród języków austroazjatyckich .
Podobnie jak chiński , jest językiem tonalnym , izolującym i analitycznym .
