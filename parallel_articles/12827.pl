Woda
Woda ( tlenek wodoru ; nazwa systematyczna IUPAC : oksydan ) – związek chemiczny o wzorze H2O , występujący w warunkach standardowych w stanie ciekłym .
W stanie gazowym wodę określa się mianem pary wodnej , a w stałym stanie skupienia – lodem .
Słowo „ woda ” jako nazwa związku chemicznego może odnosić się do każdego stanu skupienia .
Woda jest bardzo dobrym rozpuszczalnikiem dla substancji polarnych .
Większość ( około 97,38 % ) występującej na Ziemi wody jest „ słona ” , to znaczy zawiera dużo rozpuszczonych soli , głównie chlorku sodu .
W naturalnej wodzie rozpuszczone są gazy atmosferyczne , z których w największym stężeniu znajduje się dwutlenek węgla .
Woda naturalna w wielu przypadkach przed zastosowaniem musi zostać uzdatniona .
Proces uzdatniania wody dotyczy zarówno wody pitnej , jak i przemysłowej .
Występowanie wody .
Woda jest jedną z najpospolitszych substancji we Wszechświecie .
Cząsteczka wody jest trzecią najbardziej rozpowszechnioną molekułą w ośrodku międzygwiazdowym , po cząsteczkowym wodorze i tlenku węgla .
Jest również szeroko rozpowszechniona w Układzie Słonecznym : stanowi istotny element budowy Ceres i księżyców lodowych krążących wokół planet-olbrzymów , jako domieszka występuje w ich atmosferach , a przypuszcza się , że duże jej ilości znajdują się we wnętrzach tych planet .
Jako lód występuje także na części planetoid , a zapewne również na obiektach transneptunowych .
Woda jest bardzo rozpowszechniona także na powierzchni Ziemi .
Występuje głównie w oceanach , które pokrywają 70,8 % powierzchni globu , ale także w rzekach , jeziorach i w postaci stałej w lodowcach .
Część wody znajduje się w atmosferze ( chmury , para wodna ) .
Niektóre związki chemiczne zawierają cząsteczki wody w swojej budowie ( hydraty – określa się ją wówczas mianem wody krystalizacyjnej ) .
Zawartość wody włączonej w strukturę minerałów w płaszczu Ziemi może przekraczać łączną zawartość wody w oceanach i innych zbiornikach powierzchniowych nawet dziesięciokrotnie .
Woda występująca w przyrodzie jest roztworem soli i gazów .
Najwięcej soli mineralnych zawiera woda morska i wody mineralne ; najmniej woda z opadów atmosferycznych .
Wodę o małej zawartości składników mineralnych nazywamy `` wodą miękką '' , natomiast zawierającą znaczne ilości soli wapnia i magnezu – `` wodą twardą '' .
Oprócz tego wody naturalne zawierają rozpuszczone substancje pochodzenia organicznego , na przykład mocznik , kwasy humusowe .
