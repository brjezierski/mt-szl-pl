Wola Wodyńska
Wola Wodyńska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wodynie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Części wsi .
Wierni Kościoła rzymskokatolickiego należą do parafii Świętych Apostołów Piotra i Pawła w Wodyniach .
Wieś szlachecka położona była w drugiej połowie XVI wieku w powiecie garwolińskim ziemi czerskiej województwa mazowieckiego .
Historia .
Zapisy historyczne potwierdzają istnienie wsi w XIV wieku .
Wieś ta istniała już dawniej w ziemi czerskiej 1478 roku .
W 1576 roku posiadała 71/2 łan .
W roku 1827 było w niej 23 domów i 292 mieszkańców .
W 1863 roku wieś wzmiankowana jest jako `` Wodyńska Wola '' - wieś i folwark w powiecie siedleckim , gmina i parafia w Wodynie , odległość 20 wiorst od Siedlec , posiadała 24 domów , 250 mieszkańców .
W roku 1863 folwark Wola Wodyńska posiadał rozległość mórg 2132 , gruntów ornych i ogrodów mórg 494 , łąk mórg 161 , pastwisk mórg 14 , wody mórg 6 , lasu mórg 1407 , nieużytków mórg 50 , budynków murowanych 2 , drewnianych 20 ; las urządzony , pokłady rudy , młyn wodny .
