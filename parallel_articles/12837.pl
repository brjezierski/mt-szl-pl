Wodzianka
Wodzianka – prosta zupa znana na Śląsku i w Polsce Środkowej .
Wodzianka występuje w wielu odmianach , ale w każdej podstawą są chleb i wrzątek .
Maciej E. Halbański w swoim `` Leksykonie sztuki kulinarnej '' podał następującą definicję wodzianki : .
Śląsk .
Wodzionka/wodzianka ( ) , inaczej sznelka ( z niem .
`` schnell '' = szybko , prędko ) , snelka , kura górnicza stanowiła dawniej jedno z podstawowych dań śląskiej kuchni .
Wodzionkę przyrządza się z czerstwego chleba , roztartego czosnku , czasem też cebuli i pietruszki , oraz tłuszczu zwierzęcego ( masła , smalcu lub słoniny , niekiedy też śmietany ) .
Dawniej wykorzystywano też łój wołowy lub sadło .
Składniki , po rozdrobnieniu , zalewane są wrzątkiem , najczęściej bezpośrednio w naczyniu ( talerzu ) , z którego zupę się spożywa .
Współcześnie wodzianka bywa doprawiana przyprawami typu Maggi , co zostało odnotowane w specyfikacji wpisanej na `` Listę produktów tradycyjnych '' wodzionki z województwa opolskiego .
Popularność wodzionki wiąże się z tradycją niewyrzucania a wykorzystywania każdego czerstwego pieczywa – symbolizowała śląską skromność oraz oszczędność .
Była bardzo popularnym daniem , spożywanym na co dzień , o różnych porach dnia , w rodzinach o różnym statusie materialnym .
Późną jesienią i zimą jadało się ją często na śniadanie .
Była też popularnym daniem postnym .
W XIX i na początku XX wieku uchodziła za synonim śląskiej biedy .
Śląskie wodzianki wpisane na `` Listę produktów tradycyjnych '' prowadzoną przez polskie Ministerstwo Rolnictwa i Rozwoju Wsi :
Polska Środkowa .
W Polsce środkowej wodzianka znana była też jako kapłonek .
Inne , podobne nazwy to : kapłon ( w Łęczyckiem ) , kapłun ( w Łowickiem ) i kapłanek ( w Łowickiem ) .
W niektórych regionach Polski nazywana była rosołem na siekierze .
W Brzezińskiem zwana była ślepym rosołem względnie rosopitkiem , w Łowickiem i na innych terenach Polski środkowej rosołkiem lub gapim rosołem .
Kapłonek ( wodzianka ) jest przykładem zup gotowanych z najtańszych i najłatwiej dostępnych składników , popularnych w czasach ogólnie panującej biedy .
Był codzienną potrawą jadaną w Polsce środkowej , często na śniadanie .
W czasie postu spożywano wodziankę nieokraszoną słoniną czy skwarkami .
26 stycznia 2009 produkt o nazwie „ Kapłonek ( wodzianka ) z Gałkowa ” został wpisany na polską `` Listę produktów tradycyjnych '' .
Produkt wytwarzany jest w miejscowości Gałków Duży w gminie Koluszki .
Była to potrawa ubogich na przednówku , stąd tradycyjna receptura jest skromna .
Według oficjalnej charakterystyki „ kapłonek ( wodzianka ) z Gałkowa ” jest gęstą , zawiesistą , słodko-kwaśną zupą z czerstwego chleba ze skwarkami .
Tradycyjnie gotuje się ją na skórce słoniny , z liściem laurowym , włoszczyzną lub kminkiem .
Zastosowanie czerstwego chleba żytniego z dobrze wypieczoną skórką nadaje potrawie charakterystyczny aromat i lekko kwaskowy smak .
Dodatkowo zupę zabiela się śmietaną .
Potrawę przyprawia się jedynie solą i pieprzem .
