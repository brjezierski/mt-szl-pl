Wojakowa
Wojakowa – wieś w Polsce , położona w województwie małopolskim , w powiecie brzeskim , w gminie Iwkowa .
W latach 1975–1998 wieś administracyjnie należała do województwa tarnowskiego .
Zabytki .
Obiekt wpisany do rejestru zabytków nieruchomych województwa małopolskiego : kościół parafialny pw .
Najświętszej Marii Panny , należący do parafii Najświętszej Maryi Panny Wniebowziętej w Wojakowej .
Powstanie tego kościoła datuje się na 1363 rok .
Początkowo był pw .
Wszystkich Świętych .
Była to budowla jednonawowa wzniesiona z miejscowego łupanego kamienia .
Cztery wieki później do kościoła dobudowano drewnianą wieżę dzwonnicy .
Zły stan techniczny wymusił w 1931 jego przebudowę .
Z pierwotnej bryły zachowało się jedynie prezbiterium oraz część wyposażenia starego kościoła .
