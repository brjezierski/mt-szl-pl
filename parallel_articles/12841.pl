Wojciech Jaruzelski
( ur .
6 lipca 1923 w Kurowie , zm .
25 maja 2014 w Warszawie ) – polski polityk komunistyczny i dowódca wojskowy , generał armii ludowego Wojska Polskiego .
Prezes Rady Ministrów ( 1981–1985 ) , przewodniczący Rady Państwa ( 1985–1989 ) , pierwszy i ostatni prezydent Polskiej Rzeczypospolitej Ludowej ( 1989 ) i pierwszy prezydent III Rzeczypospolitej Polskiej ( 1989–1990 ) .
Szef Głównego Zarządu Politycznego Wojska Polskiego ( 1960–1965 ) , szef Sztabu Generalnego Wojska Polskiego ( 1965–1968 ) , minister obrony narodowej ( 1968–1983 ) , przewodniczący Wojskowej Rady Ocalenia Narodowego ( 1981–1983 ) , I sekretarz Komitetu Centralnego Polskiej Zjednoczonej Partii Robotniczej ( 1981–1989 ) .
Członek Biura Politycznego KC PZPR ( 1971–1989 ) , poseł na Sejm PRL III , IV , V , VI , VII , VIII i IX kadencji ( 1961–1989 ) , członek prezydium Ogólnopolskiego Komitetu Frontu Jedności Narodu w latach 1981–1983 .
Przewodniczący Komitetu Obrony Kraju – zwierzchnik Sił Zbrojnych , współtwórca Patriotycznego Ruchu Odrodzenia Narodowego .
W nocy 12/13 grudnia 1981 stanął na czele Wojskowej Rady Ocalenia Narodowego , która wprowadziła stan wojenny ( 1981–1983 ) i sprawowała faktyczną władzę w Polsce podczas jego trwania .
Określany z tej racji jako dyktator wojskowy .
