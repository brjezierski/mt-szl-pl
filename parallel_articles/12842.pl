Wojciech Kilar
Wojciech Kilar ( ur .
17 lipca 1932 we Lwowie , zm .
29 grudnia 2013 w Katowicach ) – polski pianista , kompozytor muzyki poważnej , religijnej i filmowej .
Kawaler Orderu Orła Białego .
Życiorys .
Urodził się 17 lipca 1932 we Lwowie .
Jego dom rodzinny znajdował się przy ul .
Sapiehy 89 .
Ojciec kompozytora , Jan Franciszek Kilar , był lekarzem , zaś matka Neonilla – aktorką teatralną .
Jako dziecko Wojciech Kilar z wielką niechęcią brał lekcje gry na fortepianie u pani Reissówny .
Po przymusowym wysiedleniu ze Lwowa , w latach 1946–1947 kontynuował naukę gry na fortepianie w Państwowej Średniej Szkole Muzycznej nr 2 w Rzeszowie u K. Mirskiego .
