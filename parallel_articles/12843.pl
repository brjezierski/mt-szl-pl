Wojciech Korfanty
Wojciech Korfanty ( ur .
jako Adalbert Korfanty 20 kwietnia 1873 w osadzie Sadzawki , zm .
17 sierpnia 1939 w Warszawie ) – polski przywódca narodowy Górnego Śląska , związany z chrześcijańską demokracją .
Jedna z najważniejszych postaci Śląska i jego walki o przyłączenie do Polski .
Jeden z ojców niepodległej Polski .
Życiorys .
Młodość .
Syn Karoliny z domu Klecha i Józefa , górnika kopalni „ Fanny ” , którzy w życiu rodzinnym pielęgnowali i rozwijali wartości polskiego języka , katolickiej wiary i śląskiego obyczaju .
Miał czwórkę rodzeństwa .
W 1879 rozpoczął naukę w niemieckiej szkole ludowej w Siemianowicach , następnie , od 1885 , uczęszczał do katowickiego Gimnazjum Królewskiego ( w budynku obecnie mieści się Liceum Ogólnokształcące im .
Marii Skłodowskiej-Curie ) , gdzie poznał Konstantego Wolnego .
W szkole tej założył tajne koło , którego celem było szerzenie kultury polskiej i znajomości literatury .
Nawiązał kontakty z działaczami z Wielkopolski .
Brał udział w propolskich zebraniach .
Negatywnie wyrażał się o Ottonie von Bismarcku , za co został relegowany 14 sierpnia 1895 z klasy maturalnej .
Szkołę średnią ukończył w grudniu 1895 , po interwencji Józefa Kościelskiego , posła do Reichstagu z Wielkopolski , jako ekstern i jeszcze w tym samym roku rozpoczął studia na politechnice w Charlottenburgu .
Jesienią 1896 przeniósł się na Królewski Uniwersytet we Wrocławiu ( `` Königliche Universität zu Breslau '' ) .
Mieszkał w pokoju wynajętym przy `` Ottostraße '' 19 ( dziś ul .
Kazimierza Jagiellończyka ) , studiował na Wydziale Filozoficznym i zaliczył dwa semestry roku akademickiego 1896/1897 .
Należał do studenckiej korporacji założonej przez Górnoślązaków studiujących na wrocławskich uczelniach – Towarzystwa Akademików Górnoślązaków , które w 1899 roku zostało rozwiązane przez władze .
Studia przerwał na dwa lata , podczas których pracował jako korepetytor u litewskiego arystokraty Zygmunta Jundziłły , któremu towarzyszył w podróży po Europie .
Dzięki korepetycjom zarobił pieniądze na dalsze studia .
Podjął studia na tym samym wydziale , miał m.in .
zajęcia z ekonomii politycznej u profesora Wernera Sombarta , z którym utrzymywał kontakty jeszcze przez wiele lat .
Mieszkał w tym czasie w pensjonacie przy `` Neue Junkerstrasse '' 5c ( ul .
Jana Kilińskiego ) .
W maju 1901 przeniósł się na ostatni semestr do Berlina , gdzie w sierpniu zakończył studia .
11 września 1902 uczestniczył w spotkaniu mającym na celu reaktywację Towarzystwa Gimnastycznego „ Sokół ” w Katowicach .
Na zebraniu większością głosów wybrano go na prezesa tej organizacji .
Korfanty udzielał się w „ Sokole ” również po 1918 r. Zwłaszcza w okresie przygotowań do powstań śląskich i plebiscytów .
W 1921 r. brał udział w I Zlocie Sokolstwa Polskiego w Warszawie .
Działalność polityczna .
W latach 1901–1908 był członkiem Ligi Narodowej , w ramach której współpracował z Romanem Dmowskim .
Od 1901 był redaktorem naczelnym „ Górnoślązaka ” .
W 1901 został aresztowany i stanął przed poznańskim sądem .
Podczas procesu bronił swoich racji argumentując , że artykuły kierowane były do szowinistów .
Odmówiono mu zwolnienia za kaucją .
Został skazany na cztery miesiące więzienia .
Przebywał w więzieniu we Wronkach za publikację artykułów pt .
`` Do Niemców '' i `` Do moich braci Górnoślązaków '' .
Władze niemieckie zwolniły go z aresztu w maju 1902 .
W 1907 lub 1908 odszedł z Endecji .
W maju 1909 uczestniczył w Poznaniu w powołaniu filii Polskiego Towarzystwa Demokratycznego , którego celem miało być zespolenie żywiołów demokratycznych ku obronie bytu narodowego oraz pracy nad rozwojem żywiołu polskiego w Rzeszy niemieckiej .
Do rady głównej weszli z Poznania m.in .
: Antoni Chłapowski , Bernard Chrzanowski , Jan Zabłocki , ze Śląska poseł Wojciech Korfanty , a z wychodźstwa Stanisław Kochowicz .
W latach 1909–1914 jego osobistym sekretarzem był Leon Nawrocki z Krotoszyna ( Wielkopolska ) , brat Władysława Nawrockiego .
W latach 1903–1912 i 1918 poseł do Reichstagu oraz pruskiego Landtagu ( 1903–1918 ) , gdzie przystąpił do Koła Polskiego , podczas gdy śląscy Polacy przystępowali do tej pory do klubu parlamentarnego Niemieckiej Partii Centrum ( `` Zentrum '' ) .
W 1905 zainicjował w Katowicach wydawanie organu prasowego górnośląskiej endecji pt .
„ Polak ” , którego był redaktorem naczelnym oraz właścicielem .
Pismo wychodziło trzy razy w tygodniu .
Z powodu ataków przeciwników politycznych i własnego środowiska zrezygnował z kandydowania w wyborach do Reichstagu w 1912 .
.
25 października 1918 wystąpił w Reichstagu z głośnym żądaniem przyłączenia do państwa polskiego wszystkich ziem polskich zaboru pruskiego i Górnego Śląska .
W latach 1918–1919 był członkiem Naczelnej Rady Ludowej stanowiącej rząd Wielkopolski podczas powstania wielkopolskiego .
W 1920 był Polskim Komisarzem Plebiscytowym na Górnym Śląsku .
Wielokrotnie występował publicznie na wiecach , agitując za Polską .
