Wojciech Kuczok
Wojciech Kuczok ( ur .
18 października 1972 w Chorzowie ) – polski prozaik , poeta , scenarzysta , krytyk filmowy , speleolog .
Życiorys .
Jest absolwentem III LO im .
Stefana Batorego w Chorzowie .
Wcześniej uczęszczał do I LO im .
Juliusza Słowackiego w Chorzowie , skąd został karnie wydalony .
Ukończył filmoznawstwo na Uniwersytecie Śląskim .
W latach 90. należał do grupy poetyckiej „ Na Dziko ” .
W 2003 r. , za powieść `` Gnój '' , otrzymał Paszport „ Polityki ” , a w 2004 Nagrodę Literacką „ Nike ” oraz nagrodę Krakowska Książka Miesiąca .
Napisał też scenariusz do filmu Magdaleny Piekorz `` Pręgi '' , który zdobył w 2004 główną nagrodę na 29 .
Festiwalu Polskich Filmów Fabularnych w Gdyni .
W latach 2009–2010 był stypendystą Deutscher Akademischer Austauschdienst .
Jest autorem monodramu `` Doktor Haust '' napisanego dla Michała Żebrowskiego .
Spektakl , który miał premierę w styczniu 2005 , wyreżyserowała Magdalena Piekorz .
Na zamówienie Teatru Nowego w Poznaniu napisał dramat `` Ambona ludu '' , który wyreżyserował Piotr Kruszczyński w 2016 .
Dramat `` Pradziady '' zrealizował jako spektakl telewizyjny Michał Zdunik w ramach projektu Teatroteka ( 2018 ) .
