Wojnachy
Wojnachy – wieś w Polsce położona w województwie podlaskim , w powiecie sokólskim , w gminie Sokółka .
Wieś Woynachy leśnictwa sokólskiego w ekonomii grodzieńskiej w drugiej połowie XVII wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa białostockiego .
Wierni kościoła rzymskokatolickiego należą do parafii Najświętszego Ciała i Krwi Chrystusa w Sokółce .
