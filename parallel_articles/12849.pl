Wojnowice ( powiat średzki )
Wojnowice ( niem .
`` Wohnwitz '' ) – wieś w Polsce , położona w województwie dolnośląskim , w powiecie średzkim , w gminie Miękinia .
W latach 1975–1998 miejscowość administracyjnie należała do województwa wrocławskiego .
Położenie .
Wieś jest położona na Wysoczynie Średzkiej , ok. 23 km na zachód od Wrocławia .
W odległości 4 km na południe , we wsi Mrozów znajduje się stacja na linii kolejowej Wrocław-Zgorzelec .
Historia wsi i zamku .
Po raz pierwszy Wojnowice wymieniono w kronikach z 1291 r. W 1351 cesarz Karol V nadał wieś rycerzowi Johannowi Skopp .
