Wojsław ( imię )
Wojsław , Wojesław , Wojisław , Wosław – staropolskie imię męskie , złożone z członów `` Woj- '' ( „ wojownik ” ) i `` -sław '' ( „ sława ” ) .
Oznacza „ tego , który zdobywa sławę w wojnach ” , „ sławnego wojownika ” .
Imię to mogło przybierać także formę Wysław .
Jego żeńskim odpowiednikiem jest Wojsława .
Wojsław imieniny obchodzi 2 listopada , jako wspomnienie bł .
Wojsława z Prus , wspominanego razem z bł .
Konradem .
