Wola Gutowska
Wola Gutowska – wieś w Polsce położona w województwie mazowieckim , w powiecie radomskim , w gminie Jedlińsk .
W podziale na regiony fizycznogeograficzne położona jest w makroregionie Wzniesień Południowomazowieckich na Równinie Radomskiej .
Leży w bezpośrednim sąsiedztwie Jedlińska przy drodze krajowej nr 7 .
Jej układ przestrzenny wyznacza rozproszona zabudowa jednorodzinna i zagrodowa .
Ważnym sektorem gospodarki Woli Gutowskiej jest rolnictwo .
Pierwsze wzmianki o miejscowości ( 1471 r. ) są ściśle związane z przedstawicielami rodu Półkoziców , którzy z biegiem czasu zaczęli posługiwać się nazwiskiem „ Gutowscy ” .
Zjawiskiem charakterystycznym dla wsi były częste zmiany własnościowe .
Po Gutowskich jednym z właścicieli majątku był Stanisław Witowski z Popowa herbu Rola .
