Wolfgang Amadeus Mozart
Wolfgang Amadeus Mozart , w formie spolszczonej Wolfgang Amadeusz Mozart ( ur .
27 stycznia 1756 w Salzburgu , zm .
5 grudnia 1791 w Wiedniu ) – austriacki ( często też spotyka się określenie `` niemiecki '' ) kompozytor i wirtuoz gry na instrumentach klawiszowych , którego twórczość związana była głównie z Wiedniem .
Razem z Haydnem oraz Beethovenem zaliczany jest do trójki tzw .
„ klasyków wiedeńskich ” .
Uznawany za jednego z najwybitniejszych i najważniejszych twórców w historii muzyki .
Życiorys .
Dzieciństwo .
Urodzony w Salzburgu , będącym wówczas stolicą niezależnego , w ramach Rzeszy , arcybiskupstwa ( dopiero w latach 1805–1810 i ponownie od 1814 tereny te stały się częścią Cesarstwa Austriackiego ) .
Był siódmym dzieckiem niemieckiego kompozytora Leopolda Mozarta oraz Anny Marii z domu Pertl .
Miał starszą o pięć lat siostrę Marię Annę nazywaną Nannerl , która także przejawiała uzdolnienia muzyczne .
Razem z nią , kiedy miała 11 lat , mały Wolfgang grywał duety klawesynowe na zasłoniętej jedwabistym materiałem klawiaturze .
Ojciec – Leopold Mozart – woził ich po całej Europie , przez co Wolfgang zaczął chorować .
W roku 1761 skomponował swój pierwszy utwór – `` Menuet i Trio '' KV 1 .
Pozostała piątka rodzeństwa zmarła w wieku niemowlęcym .
W wieku trzech lat Wolfgang , słysząc grę siostry na klawesynie , zabawiał się wyszukiwaniem tercji na tym instrumencie ( o czym później wspominała jego siostra ) .
Od piątego roku życia uczył się gry na klawesynie pod kierunkiem swojego ojca .
