Wolin ( przystanek kolejowy )
Wolin - kolejowy przystanek osobowy w Wolinie , w województwie zachodniopomorskim , w powiecie kamieńskim .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2019 przystanek obsługiwał 300–499 pasażerów na dobę .
10 czerwca 2018 roku została zmieniona nazwa przystanku osobowego z `` Wolin Pomorski '' na `` Wolin '' .
Przez przystanek przebiega zelektryfikowana linia kolejowa Szczecin Dąbie - Świnoujście Port .
Na przystanku zatrzymują się jedynie regionalne pociągi osobowe relacji Świnoujście Port- Szczecin Główny i Świnoujście Port- Poznań Główny .
