Wollongong
Wollongong – miasto satelickie dla Sydney , położone na wschodnim wybrzeżu Australii w stanie Nowa Południowa Walia .
Graniczy ze stolicą stanu od północnej strony , natomiast śródmieście położone jest ok. 68 km na południe od centrum Sydney w części wybrzeża zwanej Illawarra .
Nazwa miasta ma pochodzenia aborygeńskie „ Wol-lon-yuh „ , po ang .
