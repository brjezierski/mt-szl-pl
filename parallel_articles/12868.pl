Uzyskał on 416 898 głosów poparcia ( 2,48 % ) , co dało mu czwarty wynik spośród dziesięciu kandydatów .
Wybory samorządowe w 2010 .
W wyborach samorządowych w 2010 WiP wspólnie m.in .
z popierającymi Stanisława Żółtka działaczami UPR startowała w ramach komitetu wyborczego „ Ruch Wyborców Janusza Korwin-Mikke ” , którego pełnomocnikiem wyborczym był Tomasz Sommer .
Komitet wystawił listy kandydatów głównie do sejmików , co miało miejsce w 13 województwach .
Najlepsze rezultaty RW JKM uzyskał w województwach małopolskim ( 2,69 % ) i mazowieckim ( 2,32 % ) .
W skali kraju kandydaci RW JKM uzyskali 1,22 % głosów , co dało 7. miejsce spośród wszystkich komitetów .
Komitet wystawił również kilku kandydatów na prezydentów miast .
Lider WiP Janusz Korwin-Mikke wystartował na prezydenta Warszawy , zajmując 4. miejsce z wynikiem 3,90 % głosów .
Program partii .
Przed wyborami w 2005 partia postulowała m.in .
:
Do wyborów w 2005 PJKM szła pod hasłem „ Precz z pięcioma B ” ( biurokracją , bezrobociem , biedą , bezprawiem , bałaganem ) .
Szczegółowy program był identyczny z programem gospodarczym Unii Polityki Realnej , zatwierdzonym przez Radę Główną UPR 27 listopada 2004 .
Organizacja partii .
Organami statutowymi partii były : konwent ( składający się z członków partii ) , prezydium rady ( składające się z prezesa , I i II wiceprezesa , sekretarza oraz skarbnika ) , rada ( mogąca liczyć od 15 do 21 osób ) oraz komisja rewizyjna ( trzyosobowa ) .
Istniała także kilkuosobowa rada honorowa .
