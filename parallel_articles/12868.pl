Wolność i Praworządność
Wolność i Praworządność ( WiP ) – polska prawicowa partia polityczna o charakterze konserwatywno-liberalnym i eurosceptycznym .
Została zarejestrowana 22 czerwca 2005 jako Platforma Janusza Korwin-Mikkego , jako tymczasowy twór na potrzeby najbliższych wyborów , po których miała zostać rozwiązana .
Utworzona z inicjatywy Janusza Korwin-Mikkego , który jednak do niej nie wstąpił , delegując doń kilkunastu swoich zaufanych współpracowników .
Ugrupowanie wzięło udział w wyborach parlamentarnych i prezydenckich w 2005 .
Z list partii startowali głównie członkowie Unii Polityki Realnej , ale także inne osoby , jak np .
Bogusław Wolniewicz czy Andrzej Pilipiuk .
Wbrew wcześniejszym zapowiedziom , PJKM nie została rozwiązana .
Od tej pory pełniła rolę koła ratunkowego dla frakcji Janusza Korwin-Mikkego , który stopniowo tracił wpływ na Unię Polityki Realnej .
W 2009 , po wystąpieniu z UPR , Janusz Korwin-Mikke przystąpił do tej partii i objął w niej przewodnictwo .
Wkrótce ugrupowanie przyjęło nazwę „ Wolność i Praworządność ” , a 9 października 2010 podjęło decyzję o połączeniu się z grupą działaczy wywodzących się z UPR i skupionych wokół Stanisława Żółtka , w celu utworzenia formacji Unia Polityki Realnej – Wolność i Praworządność ( obecny Kongres Nowej Prawicy ) .
Partia ta została zarejestrowana 25 marca 2011 , a dzień wcześniej decyzją sądu WiP przestała istnieć .
Historia partii .
Po nieudanych próbach wejścia w koalicję UPR z PO , PiS lub LPR oraz problemami wewnętrznymi UPR Janusz Korwin-Mikke zdecydował się założyć partię pod własnym imieniem .
Nazwa partii celowo miała nasuwać skojarzenie z Januszem Korwin-Mikkem – gdyż , według niego samego , dużo więcej ludzi zna jego nazwisko niż UPR .
22 czerwca 2005 została zarejestrowana partia o nazwie „ Platforma Janusza Korwin-Mikke ” .
Partia w wyborach do Sejmu RP V kadencji nie przekroczyła 5 % progu wyborczego .
W 2006 Platforma Janusza Korwin-Mikke zawiesiła działalność , jednak nie została wykreślona z rejestru partii politycznych .
28 marca 2009 Janusz Korwin-Mikke ogłosił pomysł reaktywacji PJKM i startu w wyborach do Parlamentu Europejskiego .
Ostatecznie koncepcja ta upadła , a Janusz Korwin-Mikke poparł komitet UPR .
17 października 2009 Janusz Korwin-Mikke wystąpił z UPR , mimo że partia ogłosiła go już kandydatem w wyborach prezydenckich .
Polityk reaktywował równocześnie ciągle będącą zarejestrowaną PJKM .
11 listopada 2009 odbył się konwent Platformy Janusza Korwin-Mikke , na którym przegłosowano zmianę nazwy na Wolność i Praworządność oraz uchwalono program partii i wybrano nowe władze .
Funkcję prezesa objął Janusz Korwin-Mikke .
30 grudnia 2009 zmianę nazwy partii z PJKM na WiP zatwierdził sąd .
Postanowieniem sądu z 6 października 2010 WiP została wyrejestrowana z ewidencji partii politycznych .
3 dni później na konwencie w Krakowie przyjęto uchwałę zalecającą członkom WiP wstępowanie do Unii Polityki Realnej Stanisława Żółtka , w celu utworzenia ugrupowania Unia Polityki Realnej – Wolność i Praworządność ( obecny Kongres Nowej Prawicy ) .
3 grudnia wniosek o rejestrację partii został złożony .
Prawna likwidacja WiP zakończyła się 24 marca 2011 , a UPR-WiP zarejestrowana została dzień później .
Janusz Korwin-Mikke został prezesem , a dotychczasowi wiceprezesi WiP objęli stanowiska II i IV wiceprezesa nowej partii .
Udział partii w wyborach .
Wybory parlamentarne w 2005 .
PJKM zarejestrowała się we wszystkich okręgach wyborczych z wyjątkiem okręgu Sieradz .
Platforma Janusza Korwin-Mikkego otrzymała w kraju w wyborach do Sejmu 185 885 głosów , tj .
1,57 % , co nie pozwoliło na otrzymanie finansowania z budżetu państwa ( wymagane 3 % głosów ) i uzyskanie miejsc w Sejmie .
Najlepszy wynik PJKM uzyskała w okręgu Sosnowiec – 2,81 % .
W wyborach do Senatu partia wystawiła kilkunastu kandydatów ( m.in .
byłego posła Władysława Reichelta ) , którzy uzyskiwali od 5,5 do 12,5 % w okręgu .
Żaden z nich nie zdobył mandatu .
