Wolos ( gr .
Βόλος , starożytne Jolkos ) − miasto w Grecji , w administracji zdecentralizowanej Tesalia-Grecja Środkowa , w regionie Tesalia , w jednostce regionalnej Magnezja , położone nad zatoką Morza Egejskiego .
Siedziba gminy Wolos .
W 2011 roku liczyło 86 046 mieszkańców .
Leży 326 kilometrów na północ od stolicy Grecji , Aten .
