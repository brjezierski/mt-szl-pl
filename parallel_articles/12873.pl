Wolta ( jezioro )
Wolta jest jednym z największych sztucznych zbiorników na świecie .
Położone w Ghanie na powierzchni prawie 8502 km² .
Najbardziej wysunięty na północ punkt leży w okolicach miasta Yapei , najbardziej na południe koło zapory Akosombo , 520 km w dół rzeki .
Zapora spiętrza wody rzek Białej Wolty i Czarnej Wolty , które dawniej zbiegały się , tworząc rzekę Wolta , obecnie mierzoną od zapory do Oceanu Atlantyckiego .
Zbiornik Wolta został utworzony w roku 1965 po zbudowaniu zapory Akosombo .
