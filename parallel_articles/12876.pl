Wolwark
Wolwark – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie nakielskim , w gminie Szubin .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 363 mieszkańców .
Jest jedenastą co do wielkości miejscowością gminy Szubin .
