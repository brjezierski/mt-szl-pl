Woody Allen
Woody Allen , właśc .
Allan Stewart Konigsberg ( ur .
1 grudnia 1935 w Nowym Jorku ) – amerykański scenarzysta , reżyser , aktor , muzyk , producent , pisarz i kompozytor .
Określany twórcą „ intelektualnej komedii ” .
Uchodzi za jednego z najbardziej uznanych amerykańskich reżyserów .
Jego filmy bazują na literaturze , seksualności , filozofii , psychologii , jego żydowskim pochodzeniu , filmie europejskim i Nowym Jorku , gdzie mieszka .
Życiorys .
Allan Stewart Konigsberg urodził się 1 grudnia 1935 na Brooklynie .
Jest synem Żydów , Nettie oraz Martina Konigsbergów .
Ma siostrę , Letty ( ur .
1943 ) , z którą pozostaje w przyjacielskich stosunkach .
Rodzice chcieli , żeby został farmaceutą lub lekarzem , on jednak od wczesnej młodości interesował się filmem ( pasję tę wyniósł z domu rodzinnego ) , słuchał też chętnie radiowych programów komediowych i oglądał występy komików .
Według Fostera Hirscha Allen zdecydował , że sam zostanie komikiem już w wieku siedmiu lat , po obejrzeniu w kinie `` Drogi do Maroka '' .
W latach 1948–1952 uczęszczał do szkoły średniej ( Midwood High School na Brooklinie ) .
Jako uczeń zdobywał raczej przeciętne oceny , szkoła budziła w nim niechęć .
Przez osiem lat uczył się w szkole języka hebrajskiego .
W wieku 16 lat nauczył się grać na klarnecie , a od 1973 zaczął regularnie występować w lokalu Michael ’ s Pub .
Poza tym występował ze swoimi programami kabaretowymi w klubach nocnych .
W okresie nastoletnim sprzedawał żarty do gazet , niedługo później zaczął tworzyć materiały do radiowej audycji Petera Linda Hayesa .
W tym okresie dorabiał , pisząc skecze dla Boba Hope ’ a , Sida Caesara i Pata Boone ’ a oraz dowcipy i gagi dla Guya Lombardo , Sammy Kayne i Arthura Murraya ; do tej pracy zatrudnił go agent David O. Albert .
W ten sposób zarabiał 25 dol .
tygodniowo , pisząc ok. 30 żartów dziennie .
W wieku 17 lat legalnie zmienił nazwisko na Woody Allen .
