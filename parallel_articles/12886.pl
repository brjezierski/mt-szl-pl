Worksop
Worksop – największe miasto w dystrykcie Bassetlaw , położone w hrabstwie Nottinghamshire w środkowej Anglii .
Najbardziej znanymi osobami pochodzącymi z miasta są : Bruce Dickinson , wokalista grupy muzycznej Iron Maiden oraz Graham Taylor , menadżer piłki nożnej .
Położenie .
Worksop jest 40-tysięcznym miastem położonym w środkowej Anglii na północy hrabstwa Nottinghamshire w dystrykcie Bassetlaw .
Przez miasto przepływa nieduża rzeka Ryton .
