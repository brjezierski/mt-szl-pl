przez Nowogród Wielki i Nową Ładogę .
Gospodarka i historia .
W latach 1915-1927 zbudowano na rzece Wołchow elektrownię wodną .
Do tego czasu jednak wody Wołchow płynęły w zasadzie z jeziora Ilmień do jeziora Ładoga , chyba że na Ładodze padały silne deszcze – w takiej bowiem sytuacji wody Ładogi z łatwością pokonywały niewielki spadek terenu u ujścia rzeki Wołchow i wtargnąwszy w jej koryto zmieniały jej bieg na odwrotny – z Ładogi do Ilmienia .
Z okolicznością tą wiązać przypuszczalnie należy znany z historii fakt , że plemiona staroskandynawskie poprzez Ładogę i Wołchow płynęły na południe , do jeziora Ilmień ( a stamtąd wyruszały z wyprawami handlowymi dalej , aż do Grecji ) .
Odcinek na rzece Wołchow musiałby być bardzo trudny do pokonania cały czas pod prąd .
Nie jest jednak rzeczą wykluczoną , że orientowano się w zmianach pogodowych i czekano na południowym brzegu Ładogi na nadejście intensywnych opadów deszczowych , aby później móc bez przeszkód płynąć na południe z prądem rzeki .
