Wołczyn ( obwód brzeski )
Wołczyn ( biał .
Воўчын ) – wieś w obwodzie brzeskim , w rejonie kamienieckim Białorusi , centrum administracyjne sielsowietu ; do 1945 w Polsce , w województwie poleskim , w powiecie brzeskim , siedziba gminy Wołczyn .
W miejscowości działają dwie parafie – prawosławna ( pw .
św .
Mikołaja Cudotwórcy ) i rzymskokatolicka ( pw .
Trójcy Przenajświętszej ) .
W Wołczynie urodzili się hetman polny litewski Wincenty Aleksander Gosiewski ( ok. 1620 ) , król Polski i wielki książę litewski Stanisław August Poniatowski ( 1732 ) , polski malarz Zygmunt Vogel ( 1764 ) .
Dobra magnackie Wołczyn przez małżeństwo Teresy Gosiewskiej weszło jako posag do majątku Kazimierza Jana Sapiehy .
