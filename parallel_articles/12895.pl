Wołkowysk ( stacja kolejowa )
Wołkowysk ( , ) – węzłowa stacja kolejowa w miejscowości Wołkowysk , w rejonie wołkowyskim , w obwodzie grodzieńskim , na Białorusi .
Stacja Wołkowysk Centralny istniała przed II wojną światową .
31 grudnia 2018 lub 5 lutego 2019 zmieniono nazwę z Wołkowysk Centralny ( , ) na Wołkowysk .
Przy stacji znajduje się lokomotywownia .
