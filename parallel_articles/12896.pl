Wołnowacha
Wołnowacha ( ) – miasto na Ukrainie w obwodzie donieckim , administracyjne centrum rejonu wołnowaskiego .
„ Miasto-bohater Ukrainy ” ( 2022 ) .
Wołnowacha znajduje się na południu obwodu donieckiego , jest węzłem kolejowym na liniach Mariupol–Donieck i Mariupol–Zaporoże .
Przez miasto przebiega droga krajowa N20 ( Słowiańsk–Mariupol ) .
Historia .
Miejscowość powstała w 1881 roku jako węzłowa stacja kolejowa na linii z Juzowki do Mariupola .
W 1938 r. Wołnowacha otrzymała prawa miejskie .
Po II wojnie światowej miasto stało się znaczącym ośrodkiem przemysłu spożywczego i budowlanego .
