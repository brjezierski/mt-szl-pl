Wołodymyr Zełenski
Wołodymyr Ołeksandrowycz Zełenski ( ; ur .
25 stycznia 1978 w Krzywym Rogu ) – ukraiński polityk , satyryk , aktor , scenarzysta , prezenter telewizyjny , producent filmowy i przedsiębiorca , z wykształcenia prawnik .
Od 2019 prezydent Ukrainy .
W lutym 2022 doszło do inwazji Rosji na Ukrainę ; postawa prezydenta Wołodymyra Zełenskiego w czasie tej inwazji przyniosła mu międzynarodowe uznanie .
Przez wielu zagranicznych komentatorów został wówczas określony jako mąż stanu i bohater narodowy .
Człowiek Roku tygodnika „ Time ” w 2022 .
Życiorys .
Pochodzenie i wykształcenie .
Urodził się w Krzywym Rogu w rodzinie ukraińskich Żydów .
Jego ojciec Ołeksandr był profesorem i specjalistą w zakresie cybernetyki .
Wołodymyr Zełenski w dzieciństwie przez kilka lat mieszkał w mieście Erdenet w Mongolii , gdzie wówczas pracował jego ojciec .
W późniejszych latach kształcił się w gimnazjum numer 95 w rodzinnej miejscowości .
Ukończył studia prawnicze w Krzyworoskim Instytucie Ekonomicznym ( filii Kijowskiego Narodowego Uniwersytetu Ekonomicznego ) , nie podjął pracy w zawodzie prawniczym .
W międzyczasie zainteresował się aktorstwem , grał w amatorskich przedstawieniach teatralnych .
Działalność zawodowa .
W połowie lat 90. dołączył do lokalnej grupy kabaretowej , biorącej udział w pojedynkach w ramach KWN ( popularnym w krajach byłego ZSRR współzawodnictwie kabaretowym ) .
Został następnie zaproszony do zespołu „ Zaporiżżia – Krywyj Rih – Tranzyt ” .
W 1997 wraz z kilkorgiem innych aktorów współtworzył i przewodniczył grupie „ 95-j kwartał ” , dla której pisał większość skeczów .
W latach 1998–2003 grupa brała udział w najwyższych rangą zawodach ukraińskiej KWN i występowała w wielu państwach rosyjskojęzycznych .
W 2003 pomiędzy grupą a firmą AMiK doszło do konfliktu , w wyniku którego zaproponowano satyrykowi porzucenie zespołu i dalsze indywidualne występy w KWN ; Wołodymyr Zełenski jednak odmówił .
