Wólka Wołyniecka
Wólka Wołyniecka – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Leży nad Muchawką dopływem Liwca .
Miejscowość podlega rzymskokatolickiej parafii św .
Józefa Robotnika w Wołyńcach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Przez wieś przebiega droga powiatowa Mościbrody – Rakowiec .
