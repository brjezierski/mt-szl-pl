Obwód wołyński
Obwód wołyński ( , `` Wołynśka obłast ' `` ) – jeden z 24 obwodów Ukrainy .
Leży w północno-zachodniej części Ukrainy , przy granicy z Polską i Białorusią .
Stolicą obwodu jest Łuck .
Obwód wołyński graniczy z : obwodami rówieńskim od wschodu i lwowskim od południa , obwodem brzeskim Białorusi od północy oraz województwem lubelskim Polski od zachodu .
Obwód został utworzony 4 grudnia 1939 r. dekretem Prezydium Rady Najwyższej ZSRR z części terytorium II Rzeczypospolitej okupowanego przez Armię Czerwoną w trakcie agresji ZSRR na Polskę i anektowanego następnie przez ZSRR .
Największymi miastami obwodu są Łuck , Kowel , Nowowołyńsk i Włodzimierz .
W czasach I i II Rzeczypospolitej tereny współczesnego obwodu należały do Polski .
Na terenie obwodu znajdują się gniazda rodowe polskich rodów magnackich : Czartoryskich ( Czartorysk ) , Woronieckich ( Woronczyn ) , Czetwertyńskich ( Czetwertnia ) i Poryckich ( Poryck ) .
Miasta .
Główne miasta obwodu – Łuck , Kowel i Włodzimierz były miastami królewskimi Korony Królestwa Polskiego .
