Wołomin
Wołomin – miasto w Polsce położone w województwie mazowieckim , siedziba powiatu wołomińskiego i gminy miejsko-wiejskiej Wołomin .
W latach 1975–1998 miasto administracyjnie należało do województwa warszawskiego .
Wołomin należy do aglomeracji warszawskiej .
Według danych na 31 grudnia 2018 r. miasto liczyło 37 138 mieszkańców .
Wołomin położony jest 20 km od centrum Warszawy na linii kolejowej do Białegostoku ( dwie stacje w Wołominie ) .
Centralnym punktem miasta jest plac 3-go maja ( dawniej przed II wojną światową plac górnośląski , a po wojnie plac 1-go maja ) .
Wołomin jest członkiem Związku Miast Polskich .
