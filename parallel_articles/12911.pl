Wrigley Company
Wrigley Company – globalny koncern produkujący słodycze .
Główna siedziba firmy mieści się w Chicago w stanie Illinois , w Stanach Zjednoczonych .
Firma powstała w roku 1891 , jej założycielem był William Wrigley Jr. , stąd też wzięła się nazwa firmy .
Wrigley Company znana jest na rynku polskim głównie za sprawą Orbit , Winterfresh , Airwaves czy Skittles .
W 2016 r. Mars Incorporated przejął firmę Wrigley Company od należącej do Warrena Buffetta Berkshire Hathaway .
W efekcie połączenia powstała organizacja Mars Wrigley Confectionery z siedzibą w Chicago ( dotychczasowej siedzibie Wrigley Company ) .
