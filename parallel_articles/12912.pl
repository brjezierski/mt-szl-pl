Wrocanka ( powiat jasielski )
Wrocanka – wieś w Polsce położona w województwie podkarpackim , w powiecie jasielskim , w gminie Tarnowiec .
W latach 1975–1998 miejscowość należała administracyjnie do województwa krośnieńskiego .
We wsi znajduje się niepubliczna szkoła podstawowa im .
