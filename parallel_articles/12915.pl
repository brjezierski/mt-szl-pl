Gwara wrocławska
Gwara wrocławska ( w gwarze `` Brasselsche Mundoart '' ; niem .
`` Breslauer Mundart , Breslauische Mundart '' ) – dawna gwara dialektu śląskiego języka niemieckiego .
Obszar występowania .
Gwara była używana we Wrocławiu .
Po drugiej wojnie światowej , w efekcie wysiedlenia Niemców z okolic Wrocławia , gwara ta jest używana sporadycznie , jedynie we Wrocławiu i przez wysiedloną diasporę .
