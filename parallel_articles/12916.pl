Wrocimowice
Wrocimowice – wieś w Polsce położona w województwie małopolskim , w powiecie proszowickim , w gminie Radziemice .
Wieś położona w końcu XVI wieku w powiecie proszowskim województwa krakowskiego była własnością klasztoru bożogrobców w Miechowie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krakowskiego .
