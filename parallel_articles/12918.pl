Września
Września ( ) – miasto w województwie wielkopolskim , siedziba gminy miejsko-wiejskiej Września i powiatu wrzesińskiego .
Położone 50 km na wschód od Poznania , nad rzeką Wrześnicą .
Ważny węzeł komunikacji drogowej i kolejowej .
Okolica równinna , rolnicza .
Prywatne miasto szlacheckie lokowane w 1357 roku położone było w XVI wieku w województwie kaliskim .
Herb miasta – biała róża na czerwonym tle .
Wywodzi się on z rodowego herbu Porajów – rodu będącego pierwszym właścicielem Wrześni .
W latach 1993–2009 we Wrześni przeprowadzano prawybory polityczne – parlamentarne , prezydenckie oraz do Parlamentu Europejskiego .
Według danych z 30 czerwca 2019 roku miasto liczyło 30 688 mieszkańców .
Położenie .
Według danych z 1 stycznia 2011 powierzchnia miasta wynosi 12,73 km² .
