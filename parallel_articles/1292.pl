Aranjuez ( stacja kolejowa )
Aranjuez – stacja kolejowa w Aranjuez , we wspólnocie autonomicznej Madryt , w Hiszpanii .
Jest częścią Cercanías Madrid , jak również Media Distancia Renfe .
Znajdują się tu 3 perony .
Stacja została otwarta w 1851 .
Obecna stacja jest częścią kompleksu kilku budynków , obsługujących nie tylko pasażerów .
