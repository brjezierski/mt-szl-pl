Wróblin ( żuraw pływający )
Wróblin – żuraw pływający wykorzystywany do prac na Odrze .
Żuraw pływający zbudowany został w Fürstenbergu w 1939 .
Po II wojnie światowej został wydobyty z Odry w pobliżu stopnia wodnego Wróblin ( po którym odziedziczył nazwę ) i stał się własnością Rejonu Dróg Wodnych we Wrocławiu .
W wyniku przekształceń i zmian żuraw wszedł w skład majątku Przedsiębiorstwa Budownictwa Hydrotechnicznego „ Odra-2 ” , gdzie pracował do 2001 .
Był używany do wydobywania zatopionych jednostek , przy pracach remontowych istniejących oraz budowie nowych stopni wodnych i mostów .
Pracował też jako pogłębiarka .
W 1998 został objęty ochroną prawną jako zabytek techniki .
W 2002 został kupiony przez Fundację Otwartego Muzeum Techniki .
Odbudowano maszynownię i urządzenia dźwigowe .
Pomieszczenia załogowe przekształcono w salę wykładową , gdzie odbywają się wystawy , prelekcje a także wykłady dla studentów Politechniki Wrocławskiej .
W 2003 żuraw był na tyle sprawny , że oddano go do dyspozycji Miejskiego Sztabu Antykryzysowego do użycia w sytuacjach kryzysowych .
Opis techniczny .
Jednostka stalowa o konstrukcji nitowanej ( niektóre elementy spawane ) .
Bez własnego napędu , przewidziana do holowania lub pchania .
Początkowo wyposażona była w ster , ale w latach 60 .
XX wieku , gdy do jej przemieszczania używano przeważnie pchaczy , ster zdemontowano .
Źródłem zasilania jest agregat prądotwórczy ze wzbudnicą , napędzany silnikiem Diesla firmy Deutz o mocy 24 kW , dający prąd stały o napięciu 110 V. W obrotnicy żurawia znajdują się dwa silniki elektryczne Siemens-Schuckert .
Jeden , o mocy 6,8 kW obraca ramię żurawia , drugi , o mocy 9,8 kW , napędza bęben wciągarki .
Żuraw może podnosić ładunki o ciężarze do 8 T ( współcześnie , wcześniej udźwig był większy ) .
Wysięgnik ma długość 11 m i może się obracać w zakresie 360° .
Maksymalna prędkość podnoszenia wynosi 9 m/min .
