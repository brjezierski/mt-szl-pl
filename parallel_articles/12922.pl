Wróblowice ( Ukraina )
Wróblowice ( ukr .
Вороблевичі , `` Worobłewyczi '' ) – wieś na Ukrainie , w obwodzie lwowskim , w rejonie drohobyckim , nad Tyśmienicą .
W 2001 roku liczyła 1929 mieszkańców .
Tarnowscy .
Majątek w przeszłości należał do rodziny Tarnowskich .
Tutaj w 1836 roku urodził się hrabia Władysław Tarnowski , pianista , kompozytor , poeta , dramaturg , tłumacz .
XIX-wieczne Wróblowice opisał m.in .
Agaton Giller pod pseudonimem B. Sulita .
Przed 1939 we Wróblowicach mieszkał Jan hr .
Tarnowski , w 1938 odznaczony krzyżem Pro Ecclesia et Pontifice .
