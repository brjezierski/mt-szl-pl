Timor Wschodni
Timor Wschodni , Demokratyczna Republika Timoru Wschodniego ( , ) – państwo na wyspie Timor w Archipelagu Malajskim .
Od zachodu graniczy z Indonezją .
Do 1975 roku kolonia portugalska pod nazwą Timor Portugalski .
W 1976 został anektowany przez Indonezję , która okupowała jego terytorium do 1999 .
Od 1999 do 2002 pod administracją Organizacji Narodów Zjednoczonych .
Niepodległość uzyskał 20 maja 2002 r. , stając się jednocześnie pierwszym suwerennym państwem powstałym w XXI w. Obok Filipin jest drugim krajem Azji Południowo-Wschodniej , w którym dominującym wyznaniem jest katolicyzm .
Terytorium państwa obejmuje głównie wschodnią część wyspy Timor , a także położoną bardziej na zachód eksklawę Oecusse o powierzchni 814 km² oraz dwie niewielkie wyspy Atauro i Jaco .
Całkowita lądowa powierzchnia kraju wynosi 15 007 km² .
Etymologia .
Według najczęściej podawanej etymologii człon „ Timor ” jest pokrewny z malajskim słowem `` timur '' , oznaczającym „ wschód ” .
Człon ten wszedł do większości języków za sprawą nazwy kolonii `` Timor Portugalski '' .
Druga część nazwy kraju w języku portugalskim brzmi „ Leste ” , co w tłumaczeniu na polski również oznacza „ wschód ” .
Oficjalna nazwa kraju w języku tetum brzmi `` Repúblika Demokrátika Timor-Leste '' , a po portugalsku – `` República Democrática de Timor-Leste '' .
Międzynarodowa Organizacja Normalizacyjna ( ISO ) za oficjalną skróconą formę w języku angielskim oraz każdym innym przyjmuje `` Timor-Leste '' ( skróty : TLS & amp ; TL ) .
