Historia .
Kolonia portugalska .
Pierwszymi Europejczykami , którzy dotarli na wyspę Timor , byli Portugalczycy w 1515 .
Są oni również pierwszymi Europejczykami , którzy się na niej osiedlili w 1520 .
Holendrzy zajęli zachodnią część wyspy w 1614 .
Kolonia portugalska otrzymała nazwę Timor Portugalski natomiast holenderska Holenderskie Indie Wschodnie .
Portugalia i Holandia toczyły spór o przebieg granicy pomiędzy koloniami .
Zakończył się on ostatecznie dopiero w 1914 r. wyrokiem Stałego Trybunału Arbitrażowego .
Granica wyznaczona w 1914 nadal obowiązuje pomiędzy Timorem Wschodnim a Indonezją .
Polityka kolonialna Portugalii wobec Timoru polegała na eksploatacji zasobów oraz niewielkiej liczbie inwestycji .
