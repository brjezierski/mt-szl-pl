Wuhan
Wuhan ( ; ) – miasto w środkowych Chinach , stolica prowincji Hubei .
Miasto jest jednym z największych ośrodków gospodarczych współczesnych Chin z dużym udziałem kapitału wielkich światowych korporacji ; rozwinięty przemysł wysokich technologii , hutnictwo metali , przemysł włókienniczy , spożywczy , maszynowy , stoczniowy , samochodowy , chemiczny oraz rafineryjny .
Centralny węzeł komunikacyjny , duży port nad rzeką Jangcy , port lotniczy Wuhan-Tianhe .
Trzecie miasto w kraju po Pekinie i Szanghaju pod względem poziomu szkolnictwa wyższego .
Geografia .
Wuhan leży u zbiegu rzeki Han do Jangcy na jej środkowym biegu .
Na zachodnim brzegu rzeki Jangcy leżą m.in .
dawne miasta Hankou i Hanyang , dzisiaj wchodzące w skład Wuhanu , zaś na wschodnim brzegu pozostałe dzielnice m.in .
Wuchang .
Ponadto w dzielnicy Huangpi do Jangcy wpływa rzeka She .
Większa część miasta leży na równinnych terenach , jedynie w północnej części dzielnicy Huangpi oraz północno-wschodniej części dzielnicy Xinzhou przeważa krajobraz górzysty .
Na terenie Wuhanu istnieje 38 jezior , w tym największe jezioro - Wschodnie o powierzchni 33 km² , które jest największym w Chinach jeziorem na obszarze miejskim .
W Wuhanie występują wszystkie cztery pory roku .
Panuje tutaj klimat subtropikalny monsunowy z obfitymi opadami deszczu .
