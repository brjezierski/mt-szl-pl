Wuppertal
Wuppertal – miasto na prawach powiatu w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf .
Leży na południe od Zagłębia Ruhry i w okolicy dużych miast : Düsseldorfu , Kolonii oraz Essen .
Największe miasto regionu Bergisches Land .
Liczy 349 721 mieszkańców ( 2010 ) .
