Wuzhou
Wuzhou ( chiń .
梧州 ; pinyin : `` Wúzhōu '' ) – miasto o statusie prefektury miejskiej w południowych Chinach , w regionie autonomicznym Kuangsi , port nad rzeką Xi Jiang .
W 2010 roku liczba mieszkańców miasta wynosiła 859 815 .
Prefektura miejska w 1999 roku liczyła 2 820 977 mieszkańców .
Ośrodek przemysłu chemicznego , drzewnego , spożywczego , stoczniowego i nowoczesnych technologii .
Miasto posiada własny port lotniczy .
Katastrofa .
21 marca 2022 samolot Boeing 737-89P rozbił się w górzystych regionach w Hrabstwie Teng , 26,4 kilometrów od centrum miasta .
Zginęły wszystkie 132 osoby .
