Lista egipskich hieroglifów
Liczba wszystkich , udokumentowanych egipskich hieroglifów w różnych okresach Starożytnego Egiptu wynosiła od kilkunastu setek w Średnim Państwie do kilku tysięcy w okresie grecko-rzymskim .
Łącznie udokumentowano ich ok. 6 tysięcy .
Najbardziej funkcjonalną listą egipskich hieroglifów jest lista Gardinera ( opublikowana w 1927 , 1950 i 1957 r. ) zawierająca 763 hieroglify w 26 kategoriach .
Inny niemiecki egiptolog , Georg Möller , stworzył bardziej rozszerzoną listę , zorganizowaną zgodnie z epokami historycznymi ( opublikowaną w 1927 i 1936 r. ) .
Dla potrzeb systemów komputerowych stworzono czcionkę blokową `` Unicode Egyptian Hierogliphs '' ( Unicode version 5.2 , 2009 ) zawierającą 1071 znaków opartą o listę Gardinera .
Od 2006 r. funkcjonuje również propozycja lingwisty Michaela Eversona rozszerzająca standard `` Unicode '' o listę Möllera .
