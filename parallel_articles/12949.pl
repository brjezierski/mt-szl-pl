Wędrogów
Wędrogów – wieś w Polsce położona w województwie łódzkim , w powiecie skierniewickim , w gminie Kowiesy .
W latach 1975–1998 miejscowość administracyjnie należała do województwa skierniewickiego .
Wędrogów jest wsią liczącą kilkadziesiąt domów , zamieszkaną przez ok. 119 osób .
Osada leży nad rzeką Chojnatką , przy drodze krajowej nr 8 ( E67 ) , która oddziela wieś od sąsiednich Kowies .
Okolicę Wędrogowa stanowią głównie pola uprawne , łąki i niewielkie lasy .
