Przez ponad tysiąc lat ( 726–1797 ) miasto było stolicą niezależnej Republiki Weneckiej , która była jedną z morskich i handlowych potęg Morza Śródziemnego .
Z okresu największego rozkwitu Republiki ( XIII-XVI wiek ) pochodzą liczne zabytki miasta , których bogactwo i forma decyduje o pierwszorzędnym znaczeniu Wenecji jako ośrodka turystyki nie tylko w skali Włoch , ale też w skali ogólnoświatowej .
Zabytki te , tworząc unikatowy zespół urbanistyczny miasta kanałów i mostów , stanowią o uznaniu Wenecji za jedną z najcenniejszych pozycji na liście światowego dziedzictwa ludzkości .
Od czerwca 2015 roku burmistrzem Wenecji jest Luigi Brugnaro .
