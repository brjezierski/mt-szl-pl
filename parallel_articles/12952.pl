Przez ponad tysiąc lat ( 726–1797 ) miasto było stolicą niezależnej Republiki Weneckiej , która była jedną z morskich i handlowych potęg Morza Śródziemnego .
Z okresu największego rozkwitu Republiki ( XIII-XVI wiek ) pochodzą liczne zabytki miasta , których bogactwo i forma decyduje o pierwszorzędnym znaczeniu Wenecji jako ośrodka turystyki nie tylko w skali Włoch , ale też w skali ogólnoświatowej .
Zabytki te , tworząc unikatowy zespół urbanistyczny miasta kanałów i mostów , stanowią o uznaniu Wenecji za jedną z najcenniejszych pozycji na liście światowego dziedzictwa ludzkości .
Od czerwca 2015 roku burmistrzem Wenecji jest Luigi Brugnaro .
We wrześniu 2020 roku wybrany na drugą kadencję .
Historia Republiki Weneckiej .
Miasto na wyspie zostało założone w 452 roku przez uciekinierów przed barbarzyńskim plemieniem Hunów .
Choć później znalazło się w strefie wpływów Bizancjum , zdołało uzyskać niezależność .
Wenecja była państwem-miastem na wzór greckich polis , jej ustrojem była republika .
Głową państwa był doża Wenecji ( `` doge '' – książę ) , teoretycznie nieusuwalny , wybierany w wyborach przez miejską arystokrację , sprawujący funkcję dożywotnio , jednak w praktyce często zmuszany do ustąpienia .
Zainteresowania polityczne Republiki Weneckiej koncentrowały się wokół wschodniej części Morza Śródziemnego .
Wenecja kontrolowała wybrzeża Adriatyku , Wyspy Egejskie , Kretę , Cypr , Korfu i inne wyspy .
Państwo było wielonarodowościowe : Włosi , Grecy , Słowianie ( Schiavoni ) , wielokulturowe i wieloreligijne .
Bliskość Rzymu wymuszała posłuszeństwo wobec papieży , ale w Wenecji panowała wolność religijna .
Szczyt konfliktu z papiestwem przypadł na pontyfikat Juliusza II : 27 kwietnia 1509 r. Juliusz II zorganizował przeciwko Wenecji Ligę w Cambrai .
Wenecja walczyła głównie przeciw muzułmańskim Turkom , prowadząc handel ze Wschodem .
W XIII wieku Marco Polo dotarł do Chin .
Wenecja zazdrośnie strzegła swojego monopolu na handel jedwabiem i przyprawami .
Jednak w XV wieku Vasco da Gama dotarł do Indii , otwierając nową , dłuższą , ale i bezpieczniejszą drogę do tego półwyspu .
W 1508 roku Portugalczycy rozpoczęli walkę z arabskimi kupcami , chcąc wyprzeć ich z Indii .
Wenecja wsparła kalifa Aleksandrii w budowie floty ; ta jednak została pokonana w bitwie morskiej niedaleko Diu ( wojskami portugalskimi dowodził Alfonso de Albuquerque ) .
Znaczenie Wenecji zaczęło słabnąć , choć do końca pozostała miastem łączącym Wschód z Zachodem , miastem sztuki i architektury .
W 1797 roku Wenecja została zdobyta przez Napoleona .
Po pokoju w Campo Formio Wenecja została włączona do Austrii .
Była najdłużej istniejącą republiką w dziejach .
Podczas Wiosny Ludów 17 marca 1848 r. proklamowano na krótko niepodległość .
W 1866 r. Wenecja została włączona do nowo powstałego Królestwa Włoch .
Miasto .
Wenecja to miasto położone na licznych bagnistych wyspach na Morzu Adriatyckim .
Wokół Wenecji znajduje się Laguna Wenecka wpisana na listę światowego dziedzictwa UNESCO .
Z tego względu jest to miasto kanałów – w większości transport odbywa się drogą wodną lub pieszo .
Ruch samochodowy możliwy jest tylko na obrzeżach Wenecji .
Najpopularniejszym środkiem lokomocji jest vaporetto ( tramwaj wodny ) .
W XIX wieku wyspę połączono ze stałym lądem linią kolejową i drogą szybkiego ruchu .
Patronem miasta jest święty Marek .
W bazylice jego imienia złożone są jego ( oraz innych świętych ) relikwie .
Światową popularność zyskał karnawał wenecki .
Istotnym elementem ubioru bawiących się są maski .
W Wenecji odbywa się ważny festiwal filmowy i biennale sztuki współczesnej .
Wenecja jest ustawicznie podtapiana .
Trwa budowa systemu śluz mającego chronić miasto i inne wyspy laguny przed zalewaniem ( MOSE Project ) .
Historyczna część Wenecji pełni głównie funkcje turystyczne .
