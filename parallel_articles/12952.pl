Muzyka w weneckich kościołach .
Przez kilka stuleci Wenecja stanowiła jeden z głównych ośrodków kultury muzycznej w Europie .
Dla Wenecjan wspaniałość sztuki stanowiła symbol potęgi miasta .
W bazylice św .
Marka rozwinął się efektowny styl muzyki na kilka chórów ( zob .
szkoła wenecka ) .
Na potrzeby uroczystych nabożeństw powstawały coraz to nowe msze , kantaty i oratoria .
W latach 1613–1667 „ maestro di capella ” w bazylice św .
Marka był najwybitniejszy ówczesny kompozytor Claudio Monteverdi .
Europejską sławą cieszyli się także organiści angażowani w bazylice ; to właśnie oni , w Wenecji , rozwinęli wirtuozowską muzykę na instrumentach klawiszowych .
Rozkwit muzyki w Wenecji związany był z instytucjami zwanymi konserwatoriami .
Były to sierocińce organizowane przy kościołach , w których chłopców i dziewczęta kształcono w rozmaitych zawodach muzycznych .
Jeden z najsławniejszych zatrudniał Antonio Vivaldiego .
Operowa stolica Europy .
W XVII wieku Wenecja uważana była za europejską „ stolicę opery ” .
To właśnie w Wenecji narodziła się i rozkwitła opera jako rozrywka dostępna dla publiczności .
W 1637 roku otwarto teatr ( Teatro Cassiano ) dostępny dla każdego , kto zapłacił za wstęp .
Na przedstawienia nowych oper z udziałem najznakomitszych ówczesnych śpiewaków zjeżdżali się w karnawale entuzjaści tej sztuki z całej Europy .
Była to atrakcja tak wielka , że u schyłku XVII wieku w Wenecji działało 16 teatrów operowych .
Inscenizacje bywały bardzo spektakularne , co umożliwiało finansowe wspieranie teatrów przez najbogatszych wenecjan .
Obliczono , że do końca XVII wieku wystawiono tu około 360 dzieł , najczęściej komponowanych specjalnie dla weneckich oper .
Pod koniec XVIII wieku Wenecja utraciła na znaczeniu , ale nadal uważana była za jedno z trzech włoskich miast ( obok Neapolu i Mediolanu ) najważniejsze dla opery .
Wystawiano tu dzieła najwybitniejszych kompozytorów .
