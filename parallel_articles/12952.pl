Ośrodek turystyczny .
Wenecja jest znanym na całym świecie ośrodkiem turystycznym i kulturalnym .
Ze względu na swoją architekturę oraz nietypowe położenie znajduje się na Liście światowego dziedzictwa UNESCO .
Bywa określana najpopularniejszym ośrodkiem turystycznym Włoch .
Turystyka stanowi podstawę gospodarki miasta i jest głównym źródłem zatrudnienia i dochodów mieszkańców miasta .
