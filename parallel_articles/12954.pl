Historia .
Okolice dzisiejszego Węgorzewa do XIII w. zamieszkiwali Galindowie .
Między 1260 a 1283 rokiem obszar został opanowany przez Krzyżaków , którzy w 1312 zbudowali w pobliżu miejsca wypływu Węgorapy z Mamr drewniany zamek , zwany Angerburg , przy którym w 1335 powstała osada przyzamkowa .
