Węgorzewo
Węgorzewo ( dawniej `` Węgobork '' , , , maz .
`` Wéngobork '' ) – miasto w woj .
warmińsko-mazurskim , w powiecie węgorzewskim , siedziba miejsko-wiejskiej gminy Węgorzewo .
Od 1 stycznia 2002 miasto powiatowe .
Węgorzewo leży na Mazurach , w historycznych Prusach Dolnych , na obszarze dawnej Galindii .
Według danych z 1 stycznia 2018 Węgorzewo liczyło 11 438 mieszkańców .
Położenie .
Według danych z 22 lipca 2015 r. powierzchnia miasta wynosiła 11 km² .
