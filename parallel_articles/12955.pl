Węgrzce ( województwo małopolskie )
Węgrzce – wieś w Polsce położona w województwie małopolskim , w powiecie krakowskim , w gminie Zielonki .
Leży na północ od Krakowa , przy DK7 do Warszawy .
Do 1954 r. istniała gmina Węgrzce .
Integralne części miejscowości : Czekaj , Sudół .
Położenie .
Węgrzce według regionalizacji fizycznogeograficznej położone są na Płaskowyżu Proszowickim ( 342.23 ) , należącym do makroregionu Niecka Nidziańska ( 342.2 ) , w podprowincji Wyżyna Małopolska ( 342 ) .
Wzdłuż zachodniego skraju miejscowości przebiega droga krajowa nr 7 , w pobliżu jej wschodniej granicy przepływa natomiast Sudół Dominikański , lewy dopływ Białuchy ( Prądnika ) .
Większa część miejscowości położona jest w otulinie Dłubniańskiego Parku Krajobrazowego .
