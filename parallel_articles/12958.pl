Wenus ( mitologia )
Wenus ( także Wenera , łac .
`` Venus '' , gr .
`` Aphrodítē '' ‘ wdzięk ’ , ‘ urok ’ ) – w mitologii rzymskiej bogini miłości .
Utożsamiana od II wieku p.n.e .
z grecką Afrodytą .
Wcześniej Wenus była italską boginią wiosny , roślinności i ogrodów warzywnych .
Głównymi atrybutami tej bogini były owoce i kwiaty .
Towarzyszyły jej gołębie .
W sztuce najczęściej była ukazywana naga lub do połowy obnażona , ale także jako piękna i młoda jadąca rydwanem zaprzężonym w gołębie .
Wenus była najbardziej czczona przez ród Juliuszów , zwłaszcza za czasów Juliusza Cezara , gdyż uważano , że bogini była antenatką Juliuszów .
Ród ten mianowicie wywodził swe korzenie od Julusa , syna Eneasza , protoplasty Rzymian , który z kolei miał być synem Trojanina Anchizesa i Wenus .
Najstarsza świątynia Wenus znajdowała się na Kapitolu .
