Arborg ( Kanada )
Arborg – miasto w Kanadzie , w prowincji Manitoba , nad rzeką Icelandic .
Zostało założone przez islandzkich osadników w XIX ( nazwa miasta oznacza po islandzku `` miasto nad rzeką '' ) .
W 1908 roku zaczęli napływać tu Polacy i Ukraińcy , zwłaszcza po zbudowaniu kolei , co doprowadziło do powstaniu społeczności wielokulturowej .
Liczba mieszkańców Arborg wynosi 1021 .
Język angielski jest językiem ojczystym dla 67,5 % , francuski dla 1,0 % mieszkańców ( 2006 ) .
