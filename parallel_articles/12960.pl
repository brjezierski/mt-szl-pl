Węże
Węże ( Serpentes ) – podrząd gadów z rzędu łuskonośnych .
Charakteryzują się wydłużonym , beznogim ciałem i aparatem szczękowym umożliwiającym niezwykle szerokie rozwarcie paszczy , a co za tym idzie połykanie ofiar w całości , brakiem błony bębenkowej i ucha środkowego .
Liczba kręgów , które mają ( podobnie jak u waranów ) dodatkowe powierzchnie stawowe , może sięgać 400 .
Węże ( jadowite i właściwe ) mają tylko jedno ( prawe ) płuco ( dusiciele mają dwa ) , wydłużoną wątrobę , nerki ułożone jedna za drugą .
Mają dobrze rozwinięty narząd Jacobsona .
Węże są grupą monofiletyczną .
Rozmnażają się płciowo .
