Wysokie ( rejon stoliński )
Wysokie ( ; ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie stolińskim , w sielsowiecie Olszany , przy drodze republikańskiej .
Dawniej majątek ziemski będący własnością Radziwiłłów .
Do 1875 wchodziło w skład ordynacji kleckiej , a następnie nieświeskiej .
W dwudziestoleciu międzywojennym leżało w Polsce , w województwie poleskim , w powiecie stolińskim , w gminie Dawidgródek .
Po II wojnie światowej w granicach Związku Sowieckiego .
Od 1991 w niepodległej Białorusi .
