Wysoka Gryfińska
Wysoka Gryfińska ( do 1945 niem .
`` Wittstock '' ) – osada w Polsce położona w województwie zachodniopomorskim , w powiecie gryfińskim , w gminie Gryfino .
Przez wieś przebiega droga wojewódzka nr 119 .
Wieś malowniczo położona pomiędzy trzema jeziorami Krzywienko , Zgniły Grzyb i Brudzień .
Do 1945 roku we wsi znajdował się dwór otoczony parkiem , po zakończeniu działań wojenny obiektu nie odbudowano .
Pozostał park położony w obniżeniu , przy skarpie wzdłuż której biegła parkowa aleja .
Z drzewostanu warto wymienić potężne dęby o średnicy pnia powyżej 1 metra , jesiony ( nawet 2,85 m ) , potężne klony i wierzby .
Pozostały drzewostan tworzą gatunki rodzime : dęby , jesiony , klon zwyczajny , wierzba biała , robinia akacjowa .
W podszyciu występują czeremcha , trzmielina europejska i dziki bez czarny .
Gatunki egzotyczne z drzewostanu wypadły , atrakcyjnym gatunkiem zielnym obcego pochodzenia jest barszcz .
Przez Wysoką Gryfińską przebiegnie droga ekspresowa S3 .
W Wysokiej Gryfińskiej planowane jest Miejsce Obsługi Podróżnych ze stacjami paliw ( po obu stronach trasy ) , z restauracją ( w kierunku południowym ) i zajazdem-motelem ( w kierunku północnym ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa szczecińskiego .
