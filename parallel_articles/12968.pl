Wyspa ( obwód iwanofrankiwski )
Wyspa ( ukr .
Виспа ) – wieś na Ukrainie w rejonie rohatyńskim obwodu iwanofrankiwskiego .
Jest rodową siedzibą rodziny Wyspiańskich herbu Bończa ( krewnych m.in .
Stanisława Wyspiańskiego ) .
Zamek .
We wsi znajdował się zamek ( obecnie na jego miejscu jest cmentarz ) , z daleka widać wyraźnie umocnione fortyfikacje starego drewnianego zamku ( mury , wieże ) .
