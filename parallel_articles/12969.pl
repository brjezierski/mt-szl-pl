Wyspy Cooka
Wyspy Cooka ( ; maoryski Wysp Cooka `` Kūki ʻĀirani '' ) – samorządne terytorium stowarzyszone Nowej Zelandii na południowym Oceanie Spokojnym w Polinezji obejmujące wyspy o tej samej nazwie .
Geografia .
W skład Wysp Cooka wchodzi 15 atoli i wysp pogrupowanych w dwa łańcuchy :
Powierzchnia Wysp Cooka wynosi 236,7 km² , natomiast obszar wyłącznej strefy ekonomicznej wysp wynosi 1 800 000 km² .
Wyspy zamieszkuje 17 434 osób ( 2016 ) .
Stolicą i największym miastem jest Avarua na wyspie Rarotonga .
Ludność .
Mieszkańcy wysp są głównie Polinezyjczykami , których określa się mianem `` Polinezyjczyków Wysp Cooka '' .
Ich liczba wynosi około 16,5 tysiąca .
Należą do kilku grup etnicznych , tworzących społeczności poszczególnych wysp lub zespołów wysp .
Posługują się pięcioma różnymi językami z rodziny polinezyjskiej oraz językiem angielskim .
