Arcachon ( zatoka )
Arcachon ( fr .
`` bassin d′Arcachon '' lub `` baie d′Arcachon '' ) – zatoka Oceanu Atlantyckiego ( Zatoki Biskajskiej ) , w południowo-zachodniej Francji , w departamencie Żyronda .
Powierzchnia zatoki wynosi ponad 150 km² ( podczas odpływu akwen kurczy się do około 50 km² ) .
Do zatoki uchodzi rzeka Eyre ( Leyre ) , tworząc rozległą deltę .
Znaczna jej część objęta jest ochroną w ramach konwencji ramsarskiej , jako ważne siedlisko ptactwa i innych zwierząt .
Od 2014 roku cała zatoka znajduje się w granicach parku morskiego `` Parc naturel marin du Bassin d'Arcachon '' .
Nad zatoką położone są miejscowości Lège-Cap-Ferret , Arès , Andernos-les-Bains , Lanton , Audenge , Biganos , Le Teich , Gujan-Mestras , La Teste-de-Buch i Arcachon .
Rozwinięte są tu hodowla ostryg , rybołówstwo i turystyka .
