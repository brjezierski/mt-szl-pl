Wyspy Marshalla
Wyspy Marshalla ( Republika Wysp Marshalla , , ) – państwo wyspiarskie na Oceanie Spokojnym , położone na północ od Nauru i Kiribati , na wschód od Mikronezji oraz na południe od amerykańskiej wyspy Wake .
Ustrój polityczny .
Prezydent Wysp Marshalla jest jednocześnie głową państwa i szefem rządu .
Jest on wybierany spośród członków marszalskiego parlamentu `` Nitijela '' przez członków tegoż parlamentu .
Po wyborze na stanowisko wyznacza swój gabinet .
Wybory do parlamentu , w którym zasiada 33 parlamentarzystów , odbywają się co cztery lata .
Historia .
Chociaż wyspy Mikronezji zasiedlone były już w 2 tysiącleciu p.n.e. , to mało wiadomo o ich wczesnej historii .
Pierwszym Europejczykiem , który przybył na Wyspy Marshalla , był hiszpański odkrywca Álvaro de Saavedra Cerón w roku 1529 .
Później wyspy uległy zapomnieniu na kilka kolejnych wieków .
W 1788 roku wylądował tu angielski kapitan John Marshall , od którego nazwiska wzięły nazwę wyspy .
W 1885 roku otwarto niemiecką placówkę handlową na wyspach .
Tym samym stały się one częścią protektoratu Nowej Gwinei Niemieckiej .
Japonia zajęła je podczas I wojny światowej i administrowała nimi jako terytorium mandatowym Ligi Narodów .
Podczas II wojny światowej wyspy , w ramach Operacji Flintlock ( Walki o Wyspy Marshalla ) zostały zajęte w 1944 roku przez Stany Zjednoczone i włączone do Powierniczych Wysp Pacyfiku .
Stany Zjednoczone zaraz po wojnie rozpoczęły na atolach Bikini i Enewetak próby z bronią jądrową , które trwały aż do lat 60 .
XX wieku .
Wielu mieszkańców Wysp Marshalla cierpi dzisiaj z powodu choroby popromiennej .
Problem rekompensat za szkody wywołane przez Amerykanów jest nadal przedmiotem rozmów dwustronnych .
W 1979 roku Republika Wysp Marshalla podpisała z rządem amerykańskim układ o wolnym stowarzyszeniu , który wszedł w życie w 1986 roku .
