Wyspy Świętego Tomasza i Książęca
Wyspy Świętego Tomasza i Książęca ( port .
`` República Democrática de São Tomé e Príncipe '' ) – państwo w Zatoce Gwinejskiej na północ od równika , składające się z pięciu wysp , z których trzy są zamieszkane .
Ustrój polityczny .
`` Zobacz więcej : '' Ustrój polityczny Wysp Świętego Tomasza i Książęcej , Podział administracyjny Wysp Świętego Tomasza i Książęcej
W 1990 roku Wyspy stały się republiką demokratyczną , na czele której stoi prezydent , wybierany w wyborach powszechnych na 5-letnią kadencję .
Parlament jednoizbowy Zgromadzenie Narodowe ( `` Assembleia Nacional '' ) , 55 osobowy , o uprawnieniach ustawodawczych i kontrolnych .
Władzę wykonawczą sprawuje prezydent z rządem , na którego czele stoi premier .
Geografia .
Wyspy Świętego Tomasza i Książęca znajdują się na Oceanie Atlantyckim w Zatoce Gwinejskiej na północny wschód od punktu , w którym równik przecina się z południkiem zerowym .
W jego skład wchodzą dwie główne wyspy , od których wzięła się nazwa kraju , a które leżą od siebie w odległości około 150 km .
Są to Wyspa Świętego Tomasza – 859 km² i Wyspa Książęca – 142 km² .
Ponadto do tej niewielkiej republiki należą także małe wysepki Pedras Tinhosas i Ilhéu das Rolas ( leżąca na równiku ) .
Wyspy są pochodzenia wulkanicznego , dosyć wysokie o górzystej powierzchni .
Klimat tropikalny .
Językiem urzędowym jest język portugalski , mieszkańcy używają także innych języków lokalnych .
Walutą tego kraju jest dobra , która dzieli się na 100 centymów .
Państwo dzieli się na dwie prowincje obejmujące każda jedną z głównych wysp .
Stolicą jest położone na Wyspie Świętego Tomasza miasto São Tomé .
Historia .
Przed przybyciem Portugalczyków wyspy były bezludne .
Zostały odkryte w 1471 roku ( João de Santarém i Pero de Escobar ) a około 1483 roku przybywali tu pierwsi portugalscy osadnicy .
Od 1522 roku kolonia portugalska .
Do połowy XVI wieku wyspy stały się dużym ośrodkiem plantacji trzciny cukrowej .
Do pracy na nich sprowadzano niewolników z zachodniej Afryki .
Od 1951 roku prowincja zamorska Portugalii .
W latach 60. powstał ruch niepodległościowy .
W 1973 roku region otrzymał autonomię , a dwa lata później niepodległość .
Władzę na niepodległych wyspach objął marksistowski Ruch Wyzwolenia Wysp Świętego Tomasza i Książęcej ( MLSTP ) .
Prezydentem został Manuel Pinto da Costa .
Rządząca partia prowadziła program reform socjalistycznych , który objął nacjonalizację plantacji .
W 1985 roku doszło do załamania gospodarczego .
Krach spowodował zmianę postawy rządzących .
Bliskie stosunki z blokiem wschodnim zastąpiono współpracą z Portugalią i Francją , a w gospodarce przyjęto wolny rynek .
W 1990 roku wprowadzono konstytucję gwarantującą system wielopartyjny .
W październiku partia rządząca odeszła od ideologii marksistowskiej i zmieniła nazwę na Ruch Wyzwolenia Wysp Świętego Tomasza i Książęcej - Partia Socjaldemokratyczna ( MLTSP - PSD ) .
W pierwszych wielopartyjnych wyborach parlamentarnych 21 stycznia 1991 zwycięstwo uzyskała Partia Konwergencji Demokratycznej .
Na czele rządu stanął Daniel Daio .
Prezydentem został kandydat niezależny Miguel Trovoada .
W przedterminowych wyborach z 1994 roku MLTSP-PSD powróciła do władzy .
Wybory prezydenckie z 2001 roku wygrał Fradique de Menezes z opozycji .
W 2002 roku rządząca formacja utworzyła rząd z partiami opozycyjnymi .
W latach 1995 i 2003 , doszło do zamachów stanu armii , w wyniku których władzę na krótko przejmowali oficerowie , szybko jednak ustępowali pod międzynarodową presją .
W 2011 roku do władzy jako kandydat niezależny powrócił Manuel Pinto da Costa .
Jego głównym rywalem był Evaristo Carvalho , były premier oraz przewodniczący Zgromadzenia Narodowego .
W pierwszej turze głosowania 17 lipca 2011 Pinto da Costa zajął pierwsze miejsce , zdobywając 35,62 % głosów poparcia , podczas gdy Carvalho uzyskał 21,79 % głosów .
W drugiej turze wyborów 7 sierpnia 2011 pokonał Carvalho , zdobywając 52,88 % głosów .
Urząd szefa państwa objął 3 września 2011 .
Gospodarka .
Główny sektor gospodarki stanowi rolnictwo oparte przede wszystkim na plantacyjnej uprawie kakaowca .
Inne rośliny uprawne to m.in .
kawowiec , palma kokosowa i olejowa , banany , wanilia , cynamonowiec oraz maniok .
Główną rolę odgrywa przemysł spożywczy i drzewny ; w kraju działają też zakłady produkujące mydło .
Emisja gazów cieplarnianych .
Emisje gazów cieplarnianych z Wysp Świętego Tomasza i Książęcej dotyczą głównie metanu i dwutlenku węgla .
W latach 90 .
XX w. i na początku XXI w. ich poziom był podobny .
W tym czasie emisje obu tych gazów – jak również podtlenku azotu – rosły , przy czym emisje dwutlenku węgla rosną szybciej i to one stały się dominujące .
Wzrost emisji dwutlenku węgla jest nieregularny .
Odpowiada za niego głównie wzrost emisji z energetyki i transportu .
Emisja równoważnika dwutlenku węgla z Komorów wyniosła w 1990 roku 0,092 Mt , z czego 0,047 Mt stanowił dwutlenek węgla .
W przeliczeniu na mieszkańca emisja wyniosła wówczas 410 kg dwutlenku węgla , a w przeliczeniu na tysiąc dolarów amerykańskich PKB – 199 kg .
W 2018 emisja dwutlenku węgla pochodzenia kopalnego wyniosła 0,150 Mt , a w przeliczeniu na mieszkańca 720 kg i w przeliczeniu na tysiąc dolarów PKB – 235 kg .
Demografia .
Wyspy zamieszkuje około 201 tys .
osób .
Częstym zjawiskiem są migracje zarobkowe ludności .
Zdecydowana większość ludności zamieszkuje Wyspę Świętego Tomasza .
Ludność chrześcijańska , wśród której przeważają katolicy , stanowi większość .
Ludność czarna pochodzenia afrykańskiego przywieziona na wyspy przez Portugalczyków stanowi 95 % , Kreole stanowią 4 % , a ludność pochodzenia europejskiego głównie portugalskiego – 1 % .
