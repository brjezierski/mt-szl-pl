Wyszczetarasiwka
Wyszczetarasiwka ( ) – wieś na Ukrainie , w obwodzie dniepropetrowskim , w rejonie nikopolskim , w hromadzie Myrowe .
W 2001 liczyła 3819 mieszkańców , spośród których 3535 wskazało jako ojczysty język ukraiński , 258 rosyjski , 1 mołdawski , 1 bułgarski , 19 białoruski , 1 gagauski , a 4 inny .
