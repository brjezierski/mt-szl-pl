Wyszków Śląski ( stacja kolejowa )
Wyszków Śląski – przystanek kolejowy w Wyszkowie Śląskim , w województwie opolskim , w powiecie nyskim , w gminie Nysa , w Polsce .
Przystanek ten przez wiele lat był nieczynny , ale po modernizacji linii kolejowej na której leży został ponownie uruchomiony 15 grudnia 2019 roku .
