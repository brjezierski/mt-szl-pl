Współzałożyciel i lider partii bolszewickiej .
Teoretyk ideologii komunizmu i twórca idei leninizmu .
Urodził się w Symbirsku w południowo-zachodniej części Rosji nad Wołgą , w rodzinie urzędnika szkolnego pochodzenia kałmucko-rosyjskiego Ilji Uljanowa i Marii z domu Blank , pochodzenia żydowsko-niemiecko-szwedzkiego .
Rewolucyjnie lewicowymi poglądami zainteresował się po egzekucji brata Aleksandra w 1887 roku , którego skazano za przygotowania do zamachu na cara .
Za udział w protestach przeciwko carskiemu reżimowi relegowany z Uniwersytetu Kazańskiego .
W kolejnych latach zainteresował się marksizmem , a po przenosinach do Petersburga stał się jednym z przywódców Socjaldemokratycznej Partii Robotniczej Rosji ( SDPRR ) .
Aresztowany za działalność wywrotową i skazany na trzy lata zesłania na Syberii .
Po odbyciu kary wyemigrował do Europy Zachodniej .
W 1903 roku stanął na czele frakcji bolszewickiej w SDPRR .
W trakcie I wojny światowej prowadził działalność antywojenną .
Po rewolucji lutowej i obaleniu caratu powrócił do Rosji , gdzie przeprowadził rewolucję październikową , wprowadzając dyktaturę partii bolszewickiej .
Po zbrojnym przejęciu władzy utworzył rząd bolszewicki i zadecydował o rozpędzeniu siłą wybranego w demokratycznych wyborach w grudniu 1917 Zgromadzenia Ustawodawczego Rosji ( konstytuanty Rosji ) .
W nowym państwie rosyjskim utworzył system monopartyjny , w którym rządzącą partią była Rosyjska Komunistyczna Partia ( bolszewików ) .
Jako przywódca wycofał Rosję z udziału w I wojnie światowej zawierając separatystyczny pokój z państwami centralnymi .
Sprzeciw wobec narzuconych siłą rządów bolszewickich wywołał trwającą od 1917 do 1922 roku wojnę domową , z której jako przywódca bolszewików wyszedł zwycięsko .
Inicjator Międzynarodówki Komunistycznej ( Kominternu ) .
W 1921 roku rozpoczął proces powojennej odbudowy kraju , inicjując Nową Politykę Ekonomiczną będącą mieszanym systemem gospodarczym określanym jako kapitalizm państwowy .
W 1922 roku połączył większą część dawnych terenów Imperium Rosyjskiego , tworząc Związek Socjalistycznych Republik Radzieckich .
