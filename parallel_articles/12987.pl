Nabokov pochodził ze szlacheckiej rodziny rosyjskiej .
Po utracie majątku w wyniku rewolucji październikowej w Rosji jego rodzina wyjechała do zachodniej Europy .
Na początku lat dwudziestych Nabokov studiował romanistykę i slawistykę w Cambridge , po ukończeniu studiów w 1923 zamieszkał w Berlinie ( Niemcy oraz środowisko emigrantów rosyjskich w Niemczech nierzadko występują w utworach Nabokova z tamtego okresu ) .
15 kwietnia 1925 Nabokov ożenił się z Verą Slonim , 10 maja 1936 urodził im się syn Dimitri .
W 1937 przenieśli się do Paryża , a w 1940 popłynęli transatlantykiem do USA , gdzie Nabokov między innymi pracował w muzeum oraz jako profesor literatury na uczelni .
Po sukcesie finansowym `` Lolity '' , w 1961 Nabokov z rodziną przeprowadził się do Montreux w Szwajcarii .
W 1963 otrzymał nominację do Oscara za scenariusz do filmu `` Lolita '' ( 1962 ) .
Od 1964 aż do śmierci mieszkał w hotelu Montreux-Palace w Montreux .
Charakterystyka twórczości .
Twórczość Nabokova należy do najwybitniejszych osiągnięć w zakresie obu literatur , do których należy , zarówno rosyjskiej , jak i angielskiej .
Powieści Nabokova eksplorują w artystycznej formie tematykę filozoficzną ( tematy takie jak estetyka , życie po śmierci , rozważania nad charakterem rzeczywistości i świadomości ) .
Przez niektórych uznawany również czasem za kpiarza literatury , Nabokov uważany jest za pisarza , który potrafi
daleko bardziej niż inni twórcy połączyć żar artysty z dyscypliną i chłodem naukowca .
Poza powieściami , trzy wersje autobiografii ( `` Tamte brzegi '' , `` Conclusive evidence '' , oraz `` Pamięci , przemów '' ) , kilka zbiorów opowiadań ( m.in .
`` Feralna trzynastka '' , `` Szczegółowy opis zachodu słońca i inne opowiadania '' ) , dramaty , nieliczne poezje , trzy tomy wykładów o literaturze europejskiej i rosyjskiej , tłumaczenia z angielskiego na rosyjski ( m.in .
tłumaczenie `` Alicji w krainie czarów '' Lewisa Carrolla ) i z rosyjskiego na angielski ( m.in .
erudycyjny przekład `` Eugeniusza Oniegina '' Aleksandra Puszkina ) , zadania szachowe i profesjonalne prace z zakresu microlepidoptera .
