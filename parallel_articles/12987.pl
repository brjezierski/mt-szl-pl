Vladimir Nabokov
Vladimir Nabokov ( właśc .
Władimir Władimirowicz Nabokow , ; ur .
w Petersburgu , zm .
2 lipca 1977 w Montreux ) – rosyjski i amerykański pisarz , tworzący do mniej więcej 1938 roku głównie w języku ojczystym , potem głównie w języku angielskim , autor kilku tekstów w języku francuskim .
Z wykształcenia filolog ( romanista i rusycysta ) , krótko studiował zoologię , przez całe życie zajmował się lepidopterologią ( liczne artykuły w czasopismach specjalistycznych , praca w harwardzkim muzeum ) , hobbystycznie układał zadania szachowe , w latach 1941–1959 był wykładowcą literatury na uczelniach amerykańskich .
Szeroką sławę przyniosła mu powieść `` Lolita '' ( opublikowana w Paryżu , 1955 ) .
Życiorys .
Urodził się 10 kwietnia 1899 według stosowanego wówczas w Rosji kalendarza juliańskiego .
