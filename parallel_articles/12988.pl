Władimir Putin urodził się 7 października 1952 w Leningradzie .
Był drugim dzieckiem Władimira Spirydonowicza Putina ( 1911–1999 ) i Marii Iwanowny z d. Szełomow ( 1911–1998 ) .
Ich pierwsze dziecko – 2-letni Wiktor – zmarł w 1941 roku , a przyczyną było prawdopodobnie niedożywienie i brak lekarstw .
Niektóre źródła uznają za wiarygodną relację Wiery Putiny ( Osiepaszwili ) , podającej się za biologiczną matkę Władimira Putina .
Według jej słów przyszły prezydent Rosji urodził się we wrześniu 1950 w obwodzie permskim jako nieślubny syn Wiery i żonatego mężczyzny Płatona Priwałowa , a od trzeciego do dziesiątego roku życia mieszkał z matką i ojczymem w Gruzji .
Chociaż mały Wołodia wychowywał się wśród lumpenproletariatu , to był rozpieszczany .
