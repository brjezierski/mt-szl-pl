Władimir Putin
Władimir Władimirowicz Putin , , wym .
( ur .
7 października 1952 w Leningradzie ) – rosyjski polityk , działacz państwowy i funkcjonariusz służb specjalnych , prezydent Federacji Rosyjskiej w latach 2000–2008 i od 2012 ; wcześniej premier Rosji ( 1999–2000 i 2008–2012 ) .
Z wykształcenia prawnik .
Funkcjonariusz KGB ( 1975–1990 ) , pracownik administracji Petersburga ( 1990–1996 ) , urzędnik administracji prezydenta Rosji Borysa Jelcyna ( 1996–1998 ) i szef FSB ( 1998–1999 ) .
Od 8 sierpnia 1999 do 7 maja 2000 premier Rosji , a od 31 grudnia 1999 także pełniący obowiązki prezydenta Rosji .
W 2000 i 2004 wybierany na prezydenta Rosji .
Po ukończeniu drugiej kadencji objął stanowisko premiera Rosji ( 2008–2012 ) .
W 2012 i 2018 ponownie wybierany na prezydenta Rosji .
W 2022 został jednogłośnie uznany za zbrodniarza wojennego przez Senat Stanów Zjednoczonych oraz Sejm Rzeczypospolitej Polskiej w związku z inwazją Rosji na Ukrainę , a w 2023 został objęty nakazem aresztowania przez Międzynarodowy Trybunał Karny .
