Włodzimierz ( Romaniuk )
Włodzimierz , imię świeckie Wasyl Romaniuk ( ur .
9 grudnia 1925 w Chomczynie , zm .
14 lipca 1995 ) – ukraiński działacz narodowy , duchowny prawosławny , w latach 1993–1995 zwierzchnik niekanonicznego Ukraińskiego Autokefalicznego Kościoła Prawosławnego .
Życiorys .
Urodził się w II Rzeczypospolitej .
Jako młody człowiek był działaczem OUN .
W 1949 r. skazany został na dwanaście lat obozu pracy na mocy wyroku NKWD .
Po wyjściu na wolność w 1959 r. ukończył moskiewskie seminarium duchowne .
