Władykaukaz
Władykaukaz ( , ) – stolica i największe miasto Republiki Północnej Osetii-Alanii , położone w południowo-wschodniej części kraju , u stóp Kaukazu , nad rzeką Terek .
Ludność składa się w większości z Osetyjczyków , Rosjan , Ormian i Gruzinów .
We Władykaukazie była duża polska diaspora i kościół .
Wcześniej nosiło nazwy Ordżonikidze ( 1931–1944 , 1954–1990 ) oraz Dzaudżikau ( 1944–1954 ) .
Historia .
Miasto zostało założone w 1784 jako twierdza w okresie rosyjskiej ekspansji na Kaukazie .
Przez wiele lat była ona główną bazą wojskową w regionie .
W 1799 zbudowano przez góry tzw .
Gruzińską Drogę Wojenną , która łączyła miasto z Gruzją na południu , zaś w 1875 powstała linia kolejowa łącząca miasto z Rostowem nad Donem ( Kolej Władykaukaska ) , która od 1900 uzyskała połączenie z azerbejdżańskim Baku .
Władykaukaz stał się głównym centrum przemysłowym regionu , przede wszystkim w zakresie przetwórstwa ropy naftowej .
W czasie wojny domowej miasto było kilkakrotnie przedmiotem walk między siłami białych i czerwonych .
W marcu 1918 r. uzbrojone oddziały robotnicze z Groznego wypędziły z niego utworzony po rewolucji lutowej i obaleniu caratu tymczasowy rząd Kozaków terskich ( Tersko-Dagestański ) .
Miasto ogłoszono stolicą Tereckiej Republiki Radzieckiej .
Latem tego samego roku antybolszewiccy powstańcy kozaccy opanowali Władykaukaz ponownie , lecz w sierpniu zostali z miasta wyparci przez popierające czerwonych oddziały inguskie .
