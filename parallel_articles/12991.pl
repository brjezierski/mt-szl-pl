Władysławowo ( rejon brasławski )
Władysławowo ( ) – część wsi Jodegale na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Mieżany .
Historia .
W dwudziestoleciu międzywojennym kolonia leżała w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie brasławskim , w gminie Dryświaty .
Według Powszechnego Spisu Ludności z 1921 roku ówczesny folwark zamieszkiwało 30 osób , wszystkie były wyznania staroobrzędowego .
Jednocześnie 1 mieszkaniec zadeklarował polską przynależność narodową , a 29 białoruską .
Było tu 6 budynków mieszkalnych .
W 1938 zamieszkiwały tu 52 osoby w 11 budynkach .
Miejscowość należała do parafii rzymskokatolickiej i prawosławnej w Dryświatach .
Podlegała pod Sąd Grodzki w m. Turmont i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Dyrświatach .
