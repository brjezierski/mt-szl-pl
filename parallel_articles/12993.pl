Wodzisław Śląski ( stacja kolejowa )
Wodzisław Śląski – stacja kolejowa położona w Wodzisławiu Śląskim otwarta dla ruchu w 1882 roku .
Przy ul .
Dworcowej znajduje się dworzec pochodzący z 1882 , który w latach 2017–2019 przeszedł remont generalny .
W otoczeniu dworca znajduje się nieczynna już i zdewastowana wieża ciśnień oraz pompa .
Dodatkowe tory są zdatne do eksploatacji .
W 2019 obsłużyła pomiędzy 700 a 999 pasażerów na dobę .
Wodzisław Śląski posiada nastawnie : WSA ( dysponująca od strony Rybnika ) oraz WSB ( dysponująca od strony Chałupek ) oraz nieczynną lokomotywownię a dawniej również zakład napraw wagonów towarowych .
Posiadał również nastawnie WSA-1 ( wykonawcza od strony Rybnika ) .
Stacja jest ważnym węzłem towarowym na trasie Katowice-Bogumin oraz dla pobliskich kopalni węgla kamiennego oraz koksowni w Radlinie .
Historia .
Historia kolei żelaznej w Wodzisławiu sięga drugiej połowy XIX w. Mieszczanie wodzisławscy chcieli uzyskać połączenie kolejowe z Katowicami i Raciborzem jednak z pominięciem Rybnika .
W 1861 roku królewskie władze rządowe wyraziły zgodę na budowę szosy z Wodzisławia przez Radlin Dolny , Obszary , Biertułtowy do dworca kolejowego Czernica ( obecnie Rydułtowy ) .
Ukończono ją po kilku latach i odtąd Wodzisław miał samodzielne połączenie z koleją zarówno do Katowic jak i Raciborza .
W 1882 roku droga ta straciła na znaczeniu w wyniku zakończenia budowy szerokotorowej linii kolejowej z Wodzisławia do Niedobczyc .
W tym samym roku miasto uzyskało połączenie poprzez Chałupki z węzłem kolejowym w Boguminie .
Dalsza rozbudowa linii kolejowej doprowadziła do połączenia Jastrzębia z Wodzisławiem a dalej z Orzeszem .
W roku 2017 stacja obsługiwała 200–299 pasażerów na dobę .
W 2018 obsłużyła już pomiędzy 500 a 699 pasażerów na dobę .
W 2019 obsługiwała pomiędzy 700 a 999 pasażerów na dobę .
Połączenia kolejowe .
W Wodzisławiu Śląskim kursują zarówno lokalne pociągi regionalne jak i dalekobieżne i międzynarodowe .
W 2022 roku kursują pociągi pośpieszne PKP Intercity do Wiednia , Grazu , Warszawy , Gdyni Przemyśl , Pragi oraz pociąg osobowe przewoźnika Koleje Śląskie do Oświęcimia , Katowic Rybnika , Chałupek i Pszczyny
Marsz śmierci 1945 .
W styczniu 1945 roku do stacji kolejowej Wodzisław ( wtedy Loslau ) dotarł marsz śmierci z obozów w Oświęcimiu .
Wodzisław był główną stacją docelową dla ewakuacji więźniów , gdyż tędy prowadzi szlak w kierunku utrzymywanych przez Niemców Moraw .
Więźniów transportowano następnie wagonami towarowymi w kierunku Austrii i Niemiec przez Bogumin i Ostrawę .
