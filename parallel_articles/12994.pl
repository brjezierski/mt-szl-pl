Włocławek
Włocławek ( , ) – miasto w centralnej Polsce , na prawach powiatu .
Trzecie co do wielkości miasto w województwie kujawsko-pomorskim , położone nad Wisłą , przy ujściu Zgłowiączki .
Jest siedzibą powiatu włocławskiego i historyczną stolicą Kujaw .
Należy do najstarszych miast w Polsce .
Jest jednym z największych ośrodków administracyjnych i gospodarczych w regionie , z dobrze rozwiniętą infrastrukturą kulturalną i sportową .
