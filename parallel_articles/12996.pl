Włodawa
Włodawa ( ) – miasto we wschodniej Polsce , w województwie lubelskim , siedziba powiatu włodawskiego ; położone na obszarze Garbu Włodawskiego , nad Włodawką i Bugiem przy granicy z Białorusią .
Prawa miejskie otrzymała w 1534 r .
Włodawa leży na Polesiu , na terenie przedrozbiorowego województwa brzeskolitewskiego wchodzącego w skład Wielkiego Księstwa Litewskiego .
W latach 1975–1998 miasto administracyjnie należało do województwa chełmskiego .
Według danych GUS z 31 grudnia 2019 r. Włodawa liczyła 13 066 mieszkańców .
