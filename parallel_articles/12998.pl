Włodzimierz Smolarek
Włodzimierz Wojciech Smolarek ( ur .
16 lipca 1957 w Aleksandrowie Łódzkim , zm .
7 marca 2012 tamże ) – polski piłkarz występujący na pozycji pomocnika lub napastnika , trener i działacz piłkarski .
Dwukrotny klubowy mistrz Polski ( 1981 , 1982 ) oraz zdobywca Pucharu Polski ( 1985 ) w barwach Widzewa Łódź .
Zdobywca Pucharu Niemiec z Eintrachtem Frankfurt z 1988 .
W latach 1980–1992 reprezentant Polski .
Członek Klubu Wybitnego Reprezentanta .
Srebrny medalista ( za zajęcie 3. miejsca ) Mistrzostw Świata 1982 , uczestnik Mistrzostw Świata 1986 .
Kariera klubowa .
Absolwent szkoły średniej .
Wychowanek klubu Włókniarz Aleksandrów Łódzki .
Grał we Włókniarzu w latach 1967-1973 , później przeszedł do Widzewa Łódź ( 1973–1977 i 1979–1986 ) , następnie grał w Legii Warszawa ( 1977–1979 ) , niemieckim Eintrachcie Frankfurt ( 1986–1988 , zdobywca pucharu Niemiec w 1988 ) oraz holenderskich klubach : Feyenoord ( 1988–1990 , bilans meczów i bramek 46–13 ) oraz FC Utrecht ( 1990–1996 , bilans meczów i bramek 165–33 ) .
Największe sukcesy Włodzimierz Smolarek święcił w Widzewie Łódź .
W I lidze w barwach Widzewa rozegrał ( 1979–1986 ) 181 meczów , strzelając 61 bramek .
Dwukrotny mistrz Polski ( 1981 , 1982 ) , zdobywca pucharu Polski ( 1985 ) .
W 1983 roku wyprzedził Zbigniewa Bońka jako najlepszego strzelca ligowego Widzewa , a potem z bilansem 61 goli w ekstraklasie był liderem aż do maja 1996 , gdy wyprzedził go Marek Koniarek .
Włodzimierz Smolarek jest uważany za jednego z najlepszych w historii zawodników Widzewa , jak również całej polskiej piłki nożnej .
Ceniony przez wszystkie pokolenia kibiców za światową klasę , ale również za niezwykłą zadziorność , waleczność , bojowość i ambicję .
Włodzimierz Smolarek to również dwukrotny uczestnik finałów mistrzostw świata .
W 1982 w Hiszpanii za zdobycie III miejsca zdobył srebrny medal , 4 lata później w Meksyku był zdobywcą jedynej bramki dla Polski .
Osiadł na stałe w Holandii , gdzie zajął się pracą trenerską , prowadził drużyny juniorskie w Feyenoordzie .
