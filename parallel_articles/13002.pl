Włóczno
Włóczno ( ) – część wsi Szybowice w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Przepływa przez nią struga Potoczyna .
W latach 1975–1998 część wsi położona była w województwie opolskim .
Historia .
Pierwsze wzmianki o Włócznie pochodzą z 1561 .
Według relacji dawnych mieszkańców wsi , nazwa miejscowości miała pochodzić od zajęć jakie wykonywali jej mieszkańcy – uprawiali len i konopie , czesali , przędli i sprzedawali włóczkę na targach w Prudniku oraz w Szybowicach .
Wieś była zamieszkiwana w większości przez katolików .
W drugiej połowie XVII wieku panią dziedziczną Włóczna była Helena Polixena von Smeskal z domu Betsch z Piskorzowa .
Przekazała ona wieś zakonowi bożogrobców w Nysie .
Wieś była własnością zakonną do 1810 , kiedy to doszło do sekularyzacji majątków klasztornych na terytorium ówczesnego Królestwa Pruskiego .
Dwa lata później , w 1812 , król Fryderyk Wilhelm III Pruski przekazał Włóczno marszałkowi Gebhardowi Leberechtowi von Blücher .
