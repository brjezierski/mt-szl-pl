Wólka Okrąglik ( przystanek kolejowy )
Wólka Okrąglik – zlikwidowany przystanek osobowy w Wólce Okrąglik na linii kolejowej nr 55 Sokołów Podlaski – Siedlce , w województwie mazowieckim , w Polsce .
Przystanek został zamknięty 1 kwietnia 1993 roku .
Obecnie po budynku stacyjnym pozostały tylko gruzy fundamentów .
