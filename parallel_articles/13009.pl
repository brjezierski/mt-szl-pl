Wąbrzeźno ( stacja kolejowa )
Wąbrzeźno – stacja kolejowa w Wąbrzeźnie , w województwie kujawsko-pomorskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
Dzisiaj znajduje się w granicach administracyjnych miasta ale w momencie budowy , w XIX wieku , była oddalona o 3 km od niego .
Stało się to powodem budowy linii Wąbrzeźno – Wąbrzeźno Miasto , którą uważa się za pierwszą zelektryfikowaną linię na ziemiach polskich .
Zasadnicza część stacji położona jest przy głównej linii Toruń – Olsztyn .
Peron lokalnej linii Wąbrzeźno - Wąbrzeźno Miasto znajdował się po drugiej stronie placu dworcowego za budynkiem stacji .
Był on jednokrawędziowy i położony na łuku toru .
Połączenie obu linii znajdowało się w rejonie towarowym stacji na południowy zachód od budynku stacyjnego i peronów .
W roku 2021 otoczenie stacji i parking zostały odnowione .
Obecnie na stacji funkcjonują dwa perony z dwiema krawędziami peronowymi .
