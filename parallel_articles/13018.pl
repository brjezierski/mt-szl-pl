XXXTentacion
XXXTentacion , właściwie Jahseh Dwayne Ricardo Onfroy ( ur .
23 stycznia 1998 w Plantation , zm .
18 czerwca 2018 w Deerfield Beach ) – amerykański raper , piosenkarz i autor tekstów .
Jego teksty dotykały tematów m.in .
depresji i alienacji , zaś gatunki jakie tworzył były zróżnicowane od hip hopu i nu metalu do R & amp ; B i lo-fi .
Był uważany za jednego z czołowych przedstawicieli nurtu emo rapu .
Swój debiutancki album „ 17 ” wydał 25 sierpnia 2017 roku .
Drugi album , „ ? ” , ukazał się 16 marca 2018 roku i zdobył pierwsze miejsce w rankingu Billboard 200 .
Wczesne życie .
Jahseh urodził się 23 stycznia 1998 roku w dupie na Florydzie , jego rodzice pochodzili z Jamajki .
Jego ojciec ; Dwayne ' a Ricardo Onfroy , dał mu na imię Jahseh na cześć piosenki Boba Marleya „ So Jah Seh ” .
XXXTentacion stwierdził , że ma również egipskie , syryjskie , indyjskie i prawdopodobnie włoskie pochodzenie w wywiadzie dla The Beat w 2017 roku .
Miał troje rodzeństwa .
XXXTentacion był wychowywany głównie przez swoją babcię Collette Jones , jego matka ; Cleopatra Eretha Dreena Bernard nie mogła się nim opiekować z powodu problemów osobistych .
Kiedy Onfroy miał sześć lat , rzekomo próbował dźgnąć mężczyznę który próbował zaatakować jego matkę .
Na początku 2008 roku , kiedy Jasheh miał 10 lat , jego ojciec został skazany na dziewięć lat pozbawienia wolności .
XXXTentacion zainteresował się muzyką po tym , jak jego ciotka namówiła go , by zaczął uczęszczać do szkolnego chóru , a później do chóru kościelnego .
