Xalapa-Enríquez
Xalapa-Enríquez ( również Jalapa ) – miasto we wschodnim Meksyku , w górach Sierra Madre Wschodnia , na wysokości 1430 metrów , stolica stanu Veracruz .
Miasto leży w centrum stanu , w odległości 105 km od największego portu w stanie Veracruz oraz około 330 km od stolicy kraju miasta Meksyk .
W 2010 roku miasto liczyło ponad 420 tys .
