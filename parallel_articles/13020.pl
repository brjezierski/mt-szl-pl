Upodobania filmowe .
W 2009 Dolan wyznał , że jednym z jego ulubionych reżyserów jest Michael Haneke , którego ceni za precyzję w pracy kamerą oraz intensywne emocjonalnie historie , określając `` Funny Games '' ( 1997 ) i `` Pianistkę '' ( 2001 ) jako jedne z jego ulubionych filmów .
Przy innej okazji Dolan stwierdził , że `` Fortepian '' ( 1993 ) Jane Campion był jego główną filmową inspiracją , a jego ulubiony film to `` Titanic '' ( 1997 ) Jamesa Camerona .
