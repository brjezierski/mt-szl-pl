Xenasmatella vaga
Xenasmatella vaga ( Fr . )
Stalpers – gatunek grzybów należący do rodziny woskóweczkowatych ( `` Xenasmataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Xenasmatiella , Xenasmataceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten został opisany w 1821 r. przez Eliasa Friesa jako `` Phlebia vaga '' .
Do rodzaju `` Xenasmatiella '' przeniósł go Joast A. Stalpers w 1996 r .
Synonimów ma ponad 40 .
