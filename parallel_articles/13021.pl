Władysław Wojewoda w 2003 r. zaproponował polską nazwę żylaczka żółtobrązowa , Franciszek Błoński w 1889 r. opisywał ten gatunek pod nazwą nalotek żółty .
Po przeniesieniu do rodzaju `` Xenasmatiella '' obydwie polskie nazwy są niespójne z nazwą naukową .
