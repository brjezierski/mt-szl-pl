Podgrzybek grubosiatkowany
Podgrzybek grubosiatkowany , podgrzybek żeberkowany ( `` Xerocomus ferrugineus '' ( Schaeff . )
Alessio ) – gatunek grzybów z rodziny borowikowatych ( `` Boletaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Xerocomus , Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1774 r. Jacob Christian von Schaeffer nadając mu nazwę `` Boletus ferrugineus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Carlo Luciano Alessio w 1985 r .
