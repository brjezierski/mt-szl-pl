Nazwa polska podgrzybek żeberkowany pojawiła się w 1993 r. w opracowaniu Alicji Borowskiej , Edmunda Garnweidnera i Aliny Skirgiełło .
W polskim piśmiennictwie mykologicznym gatunek ten opisywany też był jako grzyb kasztanowaty i podgrzybek zajączek odmiana grubosiatkowana .
W niektórych atlasach grzybów opisywany jest jako podgrzybek żeberkowaty .
