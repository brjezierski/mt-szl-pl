Xinxiang
Xinxiang ( chiń .
新乡 ; pinyin : `` Xīnxiāng '' ) – miasto o statusie prefektury miejskiej w Chinach , w prowincji Henan .
W 2010 roku liczba mieszkańców miasta wynosiła 743 854 .
Prefektura miejska w 1999 roku liczyła 5 331 760 mieszkańców .
Ośrodek przemysłu spożywczego i włókienniczego .
Stolica rzymskokatolickiej prefektury apostolskiej Xinxiang .
Historia .
Historia miasta sięga ponad 1800 lat ; wówczas w okolicy miasta rozegrała się bitwa pod Muye , która doprowadziła do zastąpienia dynastii Shang przez dynastię Zhou .
W czasie rządów dynastii Sui , miasto rozwinęło się dzięki dużemu rynkowi , który zachęcał do przybycia kupców z sąsiednich wiosek .
W latach 1949-52 było stolicą prowincji Pingyuan .
Od 1950 roku w Xinxiang rozwija się przemysł , przez co obecnie Xinxiang posiada ponad 6 milionów ludzi i jest jednym z większych centrów przemysłowych w Henanie .
