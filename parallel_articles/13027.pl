Xochiquetzal
Xochiquetzal [ ] ( nah .
: „ kwieciste pióro ” , „ ukwiecone drogocenne zielone pióro ” lub „ ukwiecone pióro kwezala ” ) – w mitologii Azteków i Tolteków bogini kwiatów , dzieci , miłości , zabawy oraz tańców .
Pierwotnie utożsamiana z Księżycem .
Patronka 20 dnia ( `` Xochitl '' ) azteckiego kalendarza , co osiem lat urządzano na jej cześć uroczyste święto z tańczącymi postaciami w maskach uosabiającymi rośliny i zwierzęta .
Była małżonką boga deszczu Tlaloca i siostrą boga kukurydzy Xochipilli .
Z powodu swojej urody została porwana z mitycznego raju Tamoanchan przez Tezcatlipocę , który uczynił ją boginią miłości .
