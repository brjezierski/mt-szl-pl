Yakima ( Indianie )
Yakima ( także `` Yakama '' , pol .
`` Jakimowie '' ) – plemię Indian północnoamerykańskich , obecnie zamieszkujące m.in .
rezerwat o powierzchni ok. 5260 km² w stanie Waszyngton .
W przeszłości byli plemieniem zbieraczy , żywiącym się m.in .
orzechami , owocami leśnymi i łososiami z rzeki Yakima .
