Arcos de Jalón ( stacja kolejowa )
Arcos de Jalón ( hiszp .
`` Estación de Arcos de Jalón '' ) – stacja kolejowa w Santa María de Huerta , w prowincji Soria we wspólnocie autonomicznej Kastylia i León , w Hiszpanii .
Obsługuje połączenia średniego zasięgu RENFE .
Położenie stacji .
Znajduje się na linii kolejowej Madryt – Barcelona w km 182 , na wysokości 976,5 m n.p.m .
Stacja .
Stacja została otwarta w dniu 25 maja 1863 wraz z otwarciem odcinka Medinaceli - Saragossa linii kolejowej przeznaczonej do połączenia Madrytu z Saragossą .
Prace były prowadzone przez Compañía de los Ferrocarriles de Madrid a Zaragoza y Alicante ( MZA ) .
W 1941 doszło do nacjonalizacji kolei w Hiszpanii i utworzenia Renfe .
Od 31 grudnia 2004 linię obsługuje Renfe , natomiast budynkiem dworca zarządza Adif .
