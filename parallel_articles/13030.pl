Yancheng ( Kaohsiung )
Yancheng ( ) – dzielnica ( ) w rejonie miejskim miasta wydzielonego Kaohsiung na Tajwanie .
23 czerwca 2009 komitet przy Ministrze Spraw Wewnętrznych Republiki Chińskiej zarekomendował scalenie dotychczasowego powiatu Kaohsiung ( ) i miasta wydzielonego Kaohsiung ( ) w jedno miasto wydzielone .
Dotychczasowe dzielnice miejskie ( ) , jak Yancheng , zachowały po scaleniu swój dotychczasowy status .
Ustawa weszła w życie 25 grudnia 2010 roku .
Populacja dzielnicy Yancheng w 2016 roku liczyła 24 659 mieszkańców – 12 444 kobiety i 12 215 mężczyzn .
Liczba gospodarstw domowych wynosiła 10 833 , co oznacza , że w jednym gospodarstwie zamieszkiwało średnio 2,28 osób .
