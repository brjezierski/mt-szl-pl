Yangzhou
Yangzhou ( chiń .
扬州 ; pinyin : `` Yángzhōu '' ) – miasto o statusie prefektury miejskiej we wschodnich Chinach , w prowincji Jiangsu , nad Wielkim Kanałem .
W 2010 roku liczba mieszkańców miasta wynosiła 541 986 .
Prefektura miejska w 1999 roku liczyła 4 473 924 mieszkańców .
Ośrodek handlu i usług , szkolnictwa wyższego , turystyki oraz przemysłu elektronicznego , elektrotechnicznego , precyzyjnego , chemicznego , włókienniczego i odzieżowego ; ponadto wyrób instrumentów muzycznych , rzemiosło artystyczne i słynne herbaciarnie .
Stolica rzymskokatolickiej prefektury apostolskiej Yangzhou .
Historia .
Miasto nabrało znaczenia w epoce Sui , kiedy zaczęło korzystać z dobrodziejstw Wielkiego Kanału .
Po upadku dynastii Tang przez krótki czas było stolicą państwa Wu .
