Yannick Carrasco
Yannick Ferreira Carrasco ( ur .
4 września 1993 w Ixelles ) – belgijski piłkarz występujący na pozycji pomocnika w hiszpańskim klubie Atlético Madryt .
Posiada obywatelstwo portugalskie i hiszpańskie .
Kariera klubowa .
Yannick dołączył do Monaco w 2010 , a zadebiutował 30 lipca 2012 roku w meczu Ligue 2 , w którym przeciwnikiem był Tours FC .
Klub z księstwa wygrał 4:0 , a Carrasco otworzył wynik wykorzystanym rzutem wolnym .
W debiutanckim sezonie wystąpił w 27 spotkaniach strzelając 6 goli i zaliczając tyle samo asyst .
Monaco wywalczyło wtedy awans do Ligue 1 .
10 lipca 2015 roku Atletico kupiło go za kwotę 20 mln € .
Jego ligowy debiut nastąpił 30 sierpnia 2015 roku w meczu z Sevillą .
W 70 minucie zmienił Ólivera Torresa .
26 lutego 2018 roku przeszedł z Atlético Madryt do chińskiego Dalian Yifang za 30 mln€ .
