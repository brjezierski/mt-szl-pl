Yantai
Yantai ( chiń .
烟台 ; pinyin : `` Yāntái '' ) – miasto o statusie prefektury miejskiej we wschodnich Chinach , w prowincji Szantung , ważny port nad Morzem Żółtym .
W 2010 roku liczba mieszkańców miasta wynosiła 811 224 .
Prefektura miejska w 1999 roku liczyła 6 447 873 mieszkańców .
Ośrodek regionu sadowniczego i uprawy winorośli ; rozwinięty przemysł spożywczy , włókienniczy , odzieżowy , skórzany , chemiczny i elektromaszynowy .
Stolica diecezji rzymskokatolickiej .
Przed II wojną światową miasto znane było na Zachodzie jako Czyfu ( ang .
`` Chefoo '' ) , od nazwy pobliskiej wyspy Zhifu .
