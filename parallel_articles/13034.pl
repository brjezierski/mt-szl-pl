Yaren ( okręg wyborczy )
Yaren jest jednym z ośmiu okręgów wyborczych Nauru .
Swoim zasięgiem obejmuje dystrykt Yaren .
Z tego okręgu wybiera się 2 członków parlamentu .
W Yaren znajduje się siedziba parlamentu .
Obecnymi reprezentantami okręgu Yaren ( po wyborach z roku 2013 ) w Parlamencie Nauru są Charmaine Scotty i Kieren Keke .
