Yarrowia lipolytica
Yarrowia lipolytica – gatunek grzybów z rzędu drożdżakowców , z monotypowego rodzaju `` Yarrowia '' .
Odmiennie od drożdży `` Saccharomyces cerevisiae '' , przetwarzających cukry proste , wykorzystuje tłuszcze jako źródło węgla i energii .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Yarrowia , Dipodascaceae , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Saccharomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1970 r .
Wick. , Kurtzman & amp ; Herman nadając mu nazwę `` Endomycopsis lipolytica '' .
Wcześniej , bo już w 1942 r. Diddens & amp ; Lodder opisywali go jako `` Candida lipolytica '' .
Później okazało się , że jest to tylko anamorfa .
