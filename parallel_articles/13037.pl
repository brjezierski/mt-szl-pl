Yatsushiro
– miasto w Japonii , w prefekturze Kumamoto , w środkowej części wyspy Kiusiu .
W mieście znajdują się ruiny zamku Yatsushiro ( są obecnie częścią chramu shintō ) .
Jest stamtąd widok na pobliskie wyspy Amakusa i morze Yatsuhiro .
