Yazoo City
Yazoo City – miasto w Stanach Zjednoczonych , w stanie Missisipi , siedziba administracyjna hrabstwa Yazoo , nad rzeką Yazoo .
Ma 14.550 mieszkańców ( 2000 ) .
Nazwa pochodzi od rzeki Yazoo , nazwanej tak przez francuskiego podróżnika Roberta La Salle .
Jest siedzibą władz hrabstwa Yazoo .
Miasto zajmuje powierzchnię 28,3 km² ( 10,9 mil kwadratowych ) .
