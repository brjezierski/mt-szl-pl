Arcybiskup
Arcybiskup ( gr .
`` αρχή , arché '' – pierwszeństwo , gr .
`` επίσκοπος , epískopos '' – nadzorca , biskup ) – tytuł honorowy lub urząd nadawany biskupom ważniejszych diecezji ( archidiecezja ) , a także jako wyróżnienie samej osoby biskupa ( `` ad personam '' ) .
Pierwotnie arcybiskup był biskupem ważnego miasta czy regionu , sprawując często pieczę duszpasterską nad całym krajem .
Miał prawo przewodniczyć synodom podległych sobie biskupów , korygować ich prace , prowadzić wybory lub mianować i wyświęcać nowych biskupów w prowincji .
W historii utworzyła się w ten sposób instytucja arcybiskupa metropolity .
Metropolitą prowincji rzymskiej jest papież .
Inne Kościoły .
Oprócz Kościoła rzymskokatolickiego arcybiskupi istnieją także w Kościołach prawosławnym , luterańskim Szwecji , anglikańskim , Kościele starokatolickim Holandii , Kościele Katolickim Mariawitów .
W Kościołach Wschodnich metropolici mają znacznie więcej władzy : wpływ na wybór biskupów , tworzenie diecezji i inne , zachowują więc część pierwotnej struktury metropolitalnej ( zob .
arcybiskup większy ) .
