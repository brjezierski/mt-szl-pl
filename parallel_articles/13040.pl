Yeosu
Yeosu ( kor .
여수시 , `` Yeosu-si '' ) – miasto w Korei Południowej , położone w prowincji Jeolla Południowa .
Składa się ze starego Yeosu City , założonego w 1949 , oraz nowego Yeocheon , założonego w 1986 .
Współrzędne geograficzne : 34°44′N , 127°44′E .
Powierzchnia zajmowana przez miasto wynosi 498 km² .
Ludność : 320 000 mieszkańców ( stan z 2002 ) .
Miasto Yeosu zajmuje półwysep Yeosu oraz obejmuje 317 wysp ( 49 zamieszkanych i 268 niezamieszkanych ) .
1 kwietnia 1998 miasta Yeosu i Yeocheon połączyły się , tworząc jeden organizm miejski – Yeosu .
