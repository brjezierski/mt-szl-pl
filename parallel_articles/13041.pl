Osioł zwyczajny
Osioł zwyczajny ( `` Equus asinus '' ) – udomowiony gatunek ssaka z rodziny koniowatych ( Equidae ) .
Charakteryzuje się dużą głową , długimi uszami , cienkim ogonem z kitą na końcu , małymi wymaganiami pokarmowymi i odpornością na brak wody .
W stanie dzikim występuje osioł nubijski ( `` Equus africanus '' ) z podgatunkami ( osioł nubijski i osioł somalijski ) .
W wielu regionach świata użytkowana przez człowieka jest jego udomowiona forma – osioł zwyczajny ( `` Equus asinus '' ) .
