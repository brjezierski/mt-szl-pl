Krzyżowanie z innymi gatunkami .
Osły mogą krzyżować się z innymi koniowatymi , ale ich potomstwo przeważnie jest bezpłodne .
Potomek ogiera osła z klaczą konia to muł , zaś klaczy osła i ogiera konia to osłomuł .
Hodowcy dopuszczają do tego typu mieszanek przez wzgląd na dużą użytkowość tych zwierząt .
Muły są bardziej wytrzymałe i silniejsze i ze względu na to ta krzyżówka jest bardziej popularna , osłomuły spotyka się rzadko .
Użytkowanie .
Osły wykorzystuje się najczęściej jako zwierzęta juczne , rzadziej jako wierzchowe lub pociągowe , a ostatnio w onoterapii .
Często są trzymane w ogrodach zoologicznych .
Wyhodowano wiele ras , różniących się ubarwieniem , wielkością i temperamentem .
Salami .
Mięso osła ( wbrew obiegowej opinii ) jest tradycyjnym składnikiem zaledwie jednego rodzaju salami – suchej , wędzonej kiełbasy z dodatkiem drobno pokrojonej słoniny i przypraw .
Obecnie w wielu krajach odchodzi się od hodowli osłów , a salami jest produkowana głównie z wieprzowiny , wołowiny , koniny lub dziczyzny .
