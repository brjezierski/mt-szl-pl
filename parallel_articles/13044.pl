Anglia
Anglia ( ) – kraj stanowiący część Zjednoczonego Królestwa Wielkiej Brytanii i Irlandii Północnej .
W przeszłości był niezależnym królestwem .
Ma granice lądowe z Walią na zachodzie i Szkocją na północy .
Kraj jest oblewany przez Morze Celtyckie i Morze Irlandzkie .
Od kontynentalnej części Europy oddzielony jest kanałem La Manche i Morzem Północnym .
Stolicą Anglii jest Londyn .
Panuje w niej król brytyjski Karol III .
Populacja kraju to ponad 56 milionów osób ( 2019 ) , większość z nich mieszka w stolicy i innych aglomeracjach .
Obszar Anglii stanowią głównie równiny i pagórki .
Na północy teren jest bardziej górzysty , np .
w Lake District czy Górach Pennińskich .
W kraju panuje klimat morski .
Temperatura jest umiarkowana , zimą rzadko schodzi poniżej zera , latem dochodzi do 32 stopni .
Poza głównym obszarem , zajmującym południową i środkową część wyspy Wielka Brytania , na Anglię składa się także ponad 100 mniejszych wysp , takich jak Scilly czy Wight .
Historia .
Począwszy od VIII wieku zamieszkana przez Anglosasów Anglia była najeżdżana przez wikingów .
Królestwa Mercja oraz Nortumbria zostały podbite kolejno w 875 i 876 roku .
W kolejnych latach król angielski Alfred Wielki wygrał kilka znaczących bitew z Duńczykami , odbierając utracone regiony .
W 1066 roku miała miejsce inwazja Normanów na Anglię .
Normanowie pod wodzą Wilhelma Zdobywcy pokonali Harolda II i w szybkim tempie podbili południowe regiony Anglii .
Po problemach z sukcesją do tronu wybuchła wojna domowa , która zakończyła się wraz z koronacją Henryka II Plantageneta w 1154 roku .
Król Edward III próbował zwiększyć siłę swojego królestwa , starając się objąć tron Francji .
Przez ponad sto lat miały miejsce konflikty zbrojne pomiędzy oboma krajami co później nazwano wojną stuletnią .
W XVI wieku król Henryk VIII Tudor nie dostał pozwolenia od papieża na rozwód z Katarzyną Aragońską .
Parlament angielski uchwalił akt supremacji , co uniezależniło kraj od papieża .
Skutkiem tego był rozłam z Kościołem rzymskokatolickim oraz ustanowienie Kościoła anglikańskiego .
W 1666 roku w Londynie wybuchł pożar ; spłonęło ponad 13 tys .
domów , jednak w krótkim okresie miasto zostało odbudowane .
Wiele z nowo powstałych budynków zaprojektował Christopher Wren .
