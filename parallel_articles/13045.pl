Enklawa ( film )
Enklawa ( film ) ( ) – film niemiecko-serbskiej produkcji z 2015 roku w reżyserii Gorana Radovanovicia .
Historia .
Głównym tematem filmu jest życie kosowskich Serbów w małych odosobnionych etnicznych enklawach .
Premiera filmu miała miejsce 19 marca 2015 roku , a w Polsce 7 lipca 2017 .
Film został w 2016 roku zgłoszony przez Serbię do Oscara dla najlepszego filmu obcojęzycznego .
Treść .
Akcja filmu rozgrywa się w 2004 roku na przełomie kwietnia i maja .
Główny bohater filmu Nenad mieszka w serbskiej enklawie w północnym Kosowie razem z ojcem Voją i umierającym dziadkiem Milutinem .
Są chrześcijanami , a ich sąsiedzi to będący muzułmanami Albańczycy .
Dlatego Nenad nie ma kolegów , a do szkoły w której jest jedynym uczniem jest wożony opancerzonym pojazdem sił stabilizacyjnych KFOR .
