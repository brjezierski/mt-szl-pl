Skoki do wody
Skoki do wody – wodna dyscyplina sportowa , która obejmuje konkurencje skoków do wody .
Rozgrywa się następujące konkurencje skoków :
Rozróżnia się więc 6 grup skoków wykonywanych w trzech pozycjach .
Skoki wykonywane są z miejsca lub z rozbiegu , w zależności od ustawienia zawodnika w momencie wybicia oraz kierunku obrotu .
Zawodnicy skaczą zarówno
O wyniku decyduje ocena poprawności skoku przez sędziów oraz współczynnik jego trudności .
Od 1899 odbywają się w tej konkurencji mistrzostwa Europy , od 1904 skoki do wody są dyscypliną olimpijską , od 1973 rozgrywane są mistrzostwa świata .
Rozwojem tej dyscypliny kieruje Międzynarodowy Komitet Skoków do Wody ( IDC ) przy Międzynarodowej Federacji Pływania ( FINA ) , a w Polsce Komisja Skoków do Wody przy Polskim Związku Pływackim .
Dzięki programowi Celebrity Splash ten sport zyskał większą popularność .
