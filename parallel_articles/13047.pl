Ententa ( czyt .
, z – porozumienie , , potrójna ententa , trójporozumienie ) – sojusz pomiędzy Francją , Wielką Brytanią i Rosją .
Do jego powstania dochodziło stopniowo .
Był odpowiedzią na zawarte w 1882 Trójprzymierze .
Historia .
Imperium Rosyjskie , Cesarstwo Niemieckie i Austro-Węgry byli uczestnikami Sojuszu Trzech Cesarzy , sformalizowanego 22 października 1873 , na mocy którego zobowiązywali się oni do wzajemnych konsultacji w razie rozbieżnych interesów , zachowania pokoju w Europie oraz szukania wspólnego rozwiązania w razie agresji państw trzecich .
Francja , odwieczny wróg Niemiec , była przez Niemcy izolowana na arenie międzynarodowej , zwłaszcza po przegranej w wojnach napoleońskich i wojnie francusko-pruskiej .
Konsekwencją zawarcia `` Sojuszu Trzech Cesarzy '' był Traktat reasekuracyjny , podpisany pomiędzy Niemcami a Rosją 18 czerwca 1887 .
W 1890 roku Rosja chciała renegocjować traktat , jednakże cesarz Niemiec , Wilhelm II nie wyraził chęci ani zgody na ponowne podpisanie traktatu .
Doprowadziło to w konsekwencji , z obawy przed Niemcami , do asekuracyjnego zbliżenia się Rosji z Francją – oponentem Niemiec .
Jako pierwszy został zawarty sojusz francusko-rosyjski ( 17 sierpnia 1892 ) .
Pomimo odmiennych systemów politycznych , udało porozumieć się III Republice Francuskiej i Imperium Rosyjskiemu .
Niemcy znalazły się w niekorzystnym położeniu pomiędzy tymi dwoma krajami .
Wielka Brytania , największe mocarstwo kolonialne świata ( zob .
Imperium brytyjskie ) , znalazła się w zagrożeniu dla swych terytoriów zamorskich , przez istniejącą dopiero od 1871 niemiecką Cesarską Marynarkę Wojenną .
Marynarka Niemiec od końca XIX wieku była niezwykle intensywnie rozbudowywana .
W latach poprzedzających I wojnę światową była już drugą potęgą morską świata , ustępując jedynie Royal Navy .
Wielka Brytania , wybierając mniejsze zło , sprzymierzyła się ze swoim odwiecznym wrogiem , Francją .
Jako drugi został zawarty układ angielsko-francuski ( tzw .
entente cordiale , 1904 ) , a następnie porozumienie rosyjsko-angielskie ( 1907 ) .
Tym samym trójporozumienie zaczęło funkcjonować w 1907 .
Układ 1907 podobnie jak 1904 był wytyczeniem stref wpływów , układem wzajemnej pomocy był jedynie układ z 1892 .
Już po wybuchu I wojny światowej do Wielkiej Brytanii , Francji i Rosji , które stanowiły trzon ententy , dołączyła szeroka koalicja 25 państw , z których nie wszystkie wzięły czynny udział w wojnie przeciwko państwom centralnym .
