Yogyakarta ( okręg )
Okręg specjalny Yogyakarta ( ) – okręg w Indonezji na Jawie o statusie równym prowincji .
Powierzchnia 3169 km² ; 3,6 mln mieszkańców ( 2015 ) ; stolica Yogyakarta .
Dzieli się na 1 okręg miejski i 4 dystrykty .
Obejmuje miasto Jogyakarta wraz z otaczającymi go rejonami .
Od 1755 roku Yogyakarta jest sułtanatem .
W 1945 roku weszła w skład Indonezji .
Rząd indonezyjski pozwolił zachować władzę sułtanowi .
