Jokohama
– drugie co do wielkości miasto w Japonii , największy port morski i jeden z większych ośrodków przemysłowych w kraju .
Leży w środkowej części wyspy Honsiu ( `` Honshū '' ) , w prefekturze Kanagawa , której jest stolicą .
Miasto ma powierzchnię 437,71 km2 .
W 2020 r. mieszkały w nim 3 778 318 osób , w 1 753 389 gospodarstwach domowych ( w 2010 r. 3 689 603 osoby , w 1 583 344 gospodarstwach domowych ) .
Miasto zajmuje półwysep znajdujący się w zachodniej części Zatoki Tokijskiej , w pobliżu ujścia rzeki Tsurumi do morza .
Należy do największego azjatyckiego megalopolis Pas Taiheiyō .
Historia .
Miasto powstało w 1858 r. przez połączenie dwóch niewielkich wiosek – Jokohama i Kanagawa , których mieszkańcy trudnili się rolnictwem i rybołówstwem .
