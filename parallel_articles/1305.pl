Język arczyński
Język arczyński ( nazwa własna ) – jeden z niewielkich języków kaukaskich , używany przez Arczynów .
Należy do zespołu samurskiego , podgrupy dagestańskiej w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Przez niektórych badaczy uważany jest za dialekt lezgińskiego .
Język ten używany jest przez maksymalnie ok. 1000 osób ( 2000 ) w osiedlu Arczin ( Arczib ) w południowym Dagestanie oraz sześciu niewielkich wioskach w okolicy .
Nazwa języka wywodzi się od nazwy wsi , w której zamieszkują użytkownicy języka .
Język ten nie wykształcił piśmiennictwa .
Jest używany wyłącznie w sytuacjach nieformalnych , w domu , wśród przyjaciół .
W charakterze języka literackiego używany jest język awarski , jako największy język literacki Dagestanu .
Niektórzy Arczynowie znają także rosyjski , jako język urzędowy Federacji Rosyjskiej .
Studia nad językiem arczyńskim zapoczątkował niemiecki kaukazolog Adolf Dirr ( 1867–1930 ) , publikując w 1908 r. w Tbilisi rozprawę pt .
