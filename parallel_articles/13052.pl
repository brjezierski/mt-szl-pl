Yonkers
Yonkers – miasto w Stanach Zjednoczonych , w stanie Nowy Jork , nad Hudsonem , w zespole miejskim Nowego Jorku .
Liczba mieszkańców : 196 tys .
( spis ludności z 2000 roku )
9 września 1923 roku urodził się tam Daniel Carleton Gajdusek , amerykański biofizyk i pediatra pochodzenia słowackiego , laureat Nagrody Nobla z dziedziny fizjologii i medycyny w roku 1976 .
26 marca 1948 roku tam urodził się Steven Tyler , wokalista grupy rockowej Aerosmith .
Z miasta pochodzi także zespół Immolation wykonujący muzykę death metal .
W Yonkers urodził się Antony Blinken , amerykański prawnik , pełniący różne funkcje za czasów prezydentury Baracka Obamy oraz Joe Bidena
W mieście rozwinął się przemysł poligraficzny , maszynowy , chemiczny , elektroniczny oraz odzieżowy .
