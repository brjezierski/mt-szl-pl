YouTube
YouTube ( skrót YT ) – serwis internetowy założony w lutym 2005 roku , który umożliwia bezpłatne umieszczanie , nadawanie na żywo i komentowanie filmów .
Od 2023 roku prezesem jest Neal Mohan .
Działanie .
Serwis używa technologii HTML5 i FLV do wyświetlania filmów zamieszczonych przez użytkowników ( tzw .
user-generated content ) , takich jak zwiastuny filmowe lub telewizyjne , teledyski , jak i dzieła amatorskie : wideoblogi i krótkie własne filmy .
Większość materiałów została załadowana na YouTube przez prywatne osoby , ale wiele przedsiębiorstw ( na przykład Columbia Broadcasting System , BBC , Universal Music Group , Polska Agencja Prasowa , Grupa TVN , CD Projekt ) , różne instytucje i organizacje oferują część swoich materiałów przez YouTube jako część programu partnerskiego .
Niezarejestrowani użytkownicy mogą oglądać filmy , podczas gdy zarejestrowani mają możliwość umieszczenia nieograniczonej liczby filmów .
Filmy , które zostaną uznane za zawierające treści nieodpowiednie dla młodszych użytkowników , są dostępne tylko dla zarejestrowanych w wieku co najmniej 18 lat .
Umieszczanie filmów zawierających pornografię , objętych prawem autorskim , propagujących nienawiść oraz zachowania przestępcze i będących zniesławieniem jest zabronione w `` warunkach korzystania z usługi '' .
Profile kont zarejestrowanych użytkowników są określane jako „ kanały ” .
Na stronach serwisu wyświetlane są reklamy .
