Yueyang
Yueyang ( chiń .
岳阳 ; pinyin : `` Yuèyáng '' ) – miasto o statusie prefektury miejskiej w środkowych Chinach , w prowincji Hunan , nad jeziorem Dongting Hu .
W 2010 roku liczba mieszkańców miasta wynosiła 524 068 .
Prefektura miejska w 1999 roku liczyła 5 206 257 mieszkańców .
Stolica rzymskokatolickiej prefektury apostolskiej Yueyang .
Historia .
Pierwotnie nosiło nazwę Hanchang , a obecną nazwę otrzymało w 210 roku p.n.e .
