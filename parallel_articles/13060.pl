Yves Leterme
Yves Camille Désiré Leterme ( wym .
[ ] ; ur .
6 października 1960 w Werviku ) – belgijski polityk , premier od 20 marca 2008 do 30 grudnia 2008 oraz ponownie od 25 listopada 2009 do 6 grudnia 2011 .
W latach 2004–2007 minister-prezydent Flandrii .
Od 21 grudnia 2007 do 20 marca 2008 wicepremier Belgii , od 17 lipca 2009 do 25 listopada 2009 minister spraw zagranicznych .
Rodzina i wykształcenie .
Yves Leterme urodził się w 1960 w Werviku w prowincji Flandria Zachodnia .
Jego ojciec był francuskojęzycznym Walonem , a matka niderlandzkojęzyczną Flamandką .
W latach 1973–1979 uczęszczał do szkoły średniej `` Sint-Vincentiuscollege '' w Ypres .
W 1981 uzyskał licencjat z prawa na Uniwersytecie Katolickim w Leuven , a w 1983 licencjat z dziedziny nauk politycznych na Uniwersytecie w Gandawie .
W 1984 ukończył studia magisterskie z zakresu prawa , a rok później studia magisterskie z zakresu administracji publicznej na Uniwersytecie w Gandawie .
W 1984 zdobył dyplom w Centre d ’ études international du Fédéralisme w Nicei .
Działalność zawodowa i polityczna do 2007 .
Na początku lat 80. zaangażował się w działalność polityczną .
