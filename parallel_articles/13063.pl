Yvonne Catterfeld
Yvonne Catterfeld ( ur .
2 grudnia 1979 w Erfurcie ) – niemiecka wokalistka i aktorka .
Życiorys .
Yvonne Catterfeld od dziecka przejawiała zdolności muzyczne , w wieku 15 lat rozpoczęła naukę gry na pianinie i flecie , pobierając jednocześnie lekcje tańca i śpiewu .
Zdała maturę w Albert-Schweitzer-Gymnasium w rodzinnym Erfurcie .
Później przez dwa lata studiowała muzykę pop i jazz w Musikhochschule Leipzig .
W 1998 wydała swoje pierwsze nagrania pod pseudonimem KIV , jednak nie zyskała rozgłosu .
W 2000 występując jako Vivianne uczestniczyła w konkursie młodych talentów `` Stimme 2000 '' , gdzie została zauważona przez menedżera muzycznego powiązanego wówczas z wytwórnią BMG – Thomasa Steina .
