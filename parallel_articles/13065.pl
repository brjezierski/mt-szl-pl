Zaanstad
Zaanstad – miasto w północno-zachodniej Holandii , w prowincji Holandia Północna , na północny zachód od Amsterdamu , nad Kanałem Morza Północnego .
Powstało w 1974 roku z połączenia miasta Zaandam , Zaandijk , Koog aan de Zaan , Wormerveer , Krommenie , Assendelft i Westzaan .
