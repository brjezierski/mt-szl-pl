Biskupice Zabaryczne
Biskupice Zabaryczne – wieś w Polsce położona w województwie wielkopolskim , w powiecie ostrzeszowskim , w gminie Mikstat .
Historia .
Miejscowość historycznie należy do ziemi wieluńskiej .
Pierwotnie związana była z Wielkopolską .
Ma metrykę średniowieczną i istnieje co najmniej od XIV wieku .
Wymieniona pierwszy raz w dokumencie zapisanym po łacinie w 1342 pod nazwami „ Byskupicze , Byskupice quod Kothlovo dicitur ” , a później także „ Byskupycze Zabaryczne ” .
Miejscowość była jednak zasiedlona znacznie wcześniej .
Archeolodzy odnaleźli bowiem na jej terenie grodzisko stożkowate .
