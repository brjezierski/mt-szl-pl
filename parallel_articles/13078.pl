Zabójstwo Jamesa Bulgera
Zabójstwo Jamesa Bulgera – morderstwo niespełna trzyletniego Jamesa Bulgera dokonane przez dwóch dziesięciolatków : Roberta Thompsona i Jona Venablesa , 12 lutego 1993 w Walton koło Liverpoolu .
James Bulger .
James Bulger urodził się 16 marca 1990 w Fazakerley Hospital w Liverpoolu , a następnie zamieszkał w Kirkby .
Był wówczas jedynym dzieckiem Ralpha Stephena Bulgera i Denise Bulger ( obecnie Fergus ) .
Wprawdzie wcześniej urodziła się dziewczynka imieniem Kirsty , lecz nie przeżyła porodu .
Mając w pamięci utratę dziecka , Denise była wobec Jamesa nadopiekuńcza .
Wbrew obawom matki James był okazem zdrowia , dzieckiem bardzo żywym , radosnym i roześmianym .
Lubił rozbawiać rodzinę i dużo biegał .
Z muzyki najbardziej lubił Michaela Jacksona i próbował tańczyć jak on .
Ulubiona piosenka Jamesa , `` Heal the World '' , była odtwarzana na jego pogrzebie .
Obecnie jest wykorzystywana przez utworzoną przez Denise Fergus organizację dobroczynną James Bulger Memorial Trust , na co Michael Jackson udzielił zgody .
Miał jasne blond włosy i błękitne oczy , z brązową plamką na tęczówce prawego .
Miał już wszystkie mleczne zęby .
Znał już litery i cyfry .
Zdarzenia z 12 lutego 1993 .
Przed porwaniem .
12 lutego 1993 Denise Bulger udała się na zakupy do centrum handlowego w Bootle , wraz ze swoją szwagierką , Nicolą Bailey , zabierając ze sobą syna .
Nicola powiedziała później , że James miał w zwyczaju często uciekać i dlatego na ogół trzymany był w dziecięcym wózku .
Tym razem jednak miały to być krótsze , najwyżej godzinne zakupy , dlatego też zrezygnowały z zabrania wózka .
Ostatni raz Denise widziała Jamesa żywego , gdy robiła zakupy w sklepie mięsnym .
Syn został pod drzwiami , nie wzięła go do środka , gdyż w sklepie nie było kolejki , a zakupy miały potrwać mniej niż minutę .
Gdy ponownie spojrzała w miejsce , gdzie przed chwilą stał James , już go tam nie było .
Tego samego dnia Robert Thompson i Jon Venables uciekli ze szkoły na wagary .
Udali się do centrum handlowego w Bootle , gdzie dokonywali drobnych kradzieży .
Ukradli m.in .
słodycze , figurki trolli , baterie i niebieską farbę .
Obydwaj chłopcy byli już wówczas znani ze sprawiania kłopotów .
Razem kradli , rzucali w okna kamieniami i wchodzili na cudze posiadłości do ogródków .
Dręczyli także zwierzęta , rzucali kamieniami w ptaki i torturowali koty .
Tego dnia mieli w planie dokonać porwania i zabicia jakiegoś dziecka .
Porwanie .
Pierwsza próba zakończyła się niepowodzeniem – matka interweniowała zanim zdążyli odejść z jej synem .
Jamesa Bulgera zobaczyli pod drzwiami sklepu mięsnego o 15:40 .
Jon Venables powiedział do niego `` Come on , boy ! ''
( „ Chodź , chłopcze ! ” ) .
Początkowo James szedł z nimi chętnie .
Wyprowadzili go z centrum handlowego na ulicę i skierowali się w stronę Kanału Leeds-Liverpool .
Na moście kazali mu uklęknąć i spojrzeć na swoje odbicie w wodzie , gdyż zamierzali wrzucić go do kanału .
Kiedy odmówił , został złapany za nogi , obrócony głową do ziemi i celowo upuszczony .
Wówczas doznał pierwszych obrażeń w postaci dużego guza i zadrapań .
James próbował im uciec , ale został złapany .
Nie chciał słuchać poleceń porywaczy , więc ci popychali go , kopali i bili .
Trójkę chłopców na liczącej ponad dwie i pół mili trasie widziało wielu ludzi .
Policja odnalazła 38 świadków , z których żaden nie powstrzymał porywaczy .
Zostali wzięci za rodzeństwo , w którym starsi bracia opiekują się młodszym .
Zabójstwo .
Do zabójstwa doszło na torach kolejowych w Walton .
Początkowo Robert i Jon zamierzali położyć Jamesa na torach i obserwować z ukrycia , jak ginie pod kołami pociągu .
James jednak nie chciał leżeć na torach i za każdym razem wstawał .
Wówczas zaczęli go torturować .
Wlali mu niebieską farbę do lewego oka , obrzucali tłuczniem i cegłami , skakali po głowie i całym ciele , bili stalowym prętem .
Rozebrali Jamesa od pasa w dół , chłopiec miał siłą ściągnięty napletek .
Badano później , czy był tu motyw seksualny i czy Jamesowi wkładano baterie w odbyt .
Nie znaleziono tam jednak żadnej ; wiadomo za to , że wkładano mu baterie w usta .
Na koniec któryś z morderców rzucił w głowę Jamesa ważącym około 10 kilogramów kawałkiem stali , używanym do łączenia szyn .
Następnie chłopcy częściowo zamaskowali ciało Jamesa i zostawili je na torach , by przejechał po nim pociąg , co ich zdaniem miało zatrzeć ślady .
James Bulger doznał 42 obrażeń , w tym 22 obrażeń głowy .
Policja znalazła ślady krwi na 27 kamieniach .
Według patologa po odejściu oprawców dwulatek mógł żyć jeszcze najwyżej kilka minut .
Przy takiej ilości i rodzaju obrażeń nie da się stwierdzić , które z nich było śmiertelne .
Śledztwo .
Gdy Denise Bulger wyszła ze sklepu mięsnego i nie zobaczyła syna , zaczęła go w panice szukać .
Natychmiast zaalarmowała też ochronę obiektu , która wkrótce potem powiadomiła policję .
Centrum handlowe zamknięto na noc .
Policja zaczęła przeglądać zapisy z monitoringu .
Z zapisów wynikało , że podczas gdy Denise szukała syna na parterze i na ulicy , był on z porywaczami na pierwszym piętrze i opuścił centrum innym wyjściem .
Natychmiast rozpoczęto poszukiwania .
Przeszukiwano kanał , nieużytki i tereny kolejowe .
Po mieście jeździły samochody , z których informowano o zaginionym dziecku przez megafony .
W telewizji odtwarzano nagranie ze sklepowych kamer .
Przepołowione przez pociąg ciało Jamesa Bulgera znaleziono 14 lutego .
Z policją skontaktowała się kobieta , która na nagraniu z monitoringu rozpoznała Jona Venablesa , o którym wiedziała , że w dniu zabójstwa planował wagary .
Venablesa i Thompsona zatrzymano 18 lutego 1993 i przesłuchiwano .
Dopiero po okazaniu dowodów przyznali się do porwania , a następnie zamordowania Jamesa Bulgera .
Proces .
Proces rozpoczął się 24 listopada 1993 .
W dniu jego rozpoczęcia o mało co nie doszło do linczu , policja musiała osłaniać samochód którym przywieziono oskarżonych , doszło do aresztowań .
Dowody winy były niepodważalne – krew na butach , niebieska farba na odzieży , nagrania z kamer , świadkowie .
Sąd uznał obydwu chłopców za winnych .
Sentencja wyroku zawierała zwrot `` Her Majesty ’ s Pleasure '' ( „ oddać Królowej według uznania ” ) , co oznaczało brak konkretnego terminu końca kary .
Złożono apelację w oparciu o fakt , że wyrok taki jest niezgodny z Europejską konwencją praw człowieka .
Europejski Trybunał Praw Człowieka uznał ten wyrok za niedopuszczalny , stwierdził też , że Thompson i Venables nie mieli uczciwego procesu .
Wyrok zmieniano kilkakrotnie , ostatecznie obydwaj sprawcy wyszli na wolność 23 czerwca 2001 .
Obydwaj otrzymali fikcyjne tożsamości , by chronić ich przed próbami linczu .
Matka Jamesa stwierdziła , że kłóci się to z jej poczuciem sprawiedliwości i ma zamiar walczyć do skutku o sprawiedliwą karę dla morderców .
Ostrzegła też , że Venables jest niebezpieczny , a jego uwolnienie było błędem .
O Robercie Thompsonie brak jest jakichkolwiek wiadomości , odkąd został uwolniony .
Jon Venables odsiedział dwa następne wyroki , w tym za posiadanie pornografii dziecięcej .
Używa czwartej już fikcyjnej tożsamości , gdyż nie potrafi utrzymać w tajemnicy , kim naprawdę jest .
W listopadzie 2017 roku został ponownie aresztowany pod zarzutem posiadania materiałów obrazujących przemoc wobec dzieci .
Dalsze losy rodziny .
Gdy miesiąc po tragicznej śmierci Jamesa Bulgera Denise zaszła w ciążę , urodził się syn , Michael James .
Zanim osiągnął wiek jednego roku , małżeństwo Bulgerów rozpadło się – nie potrafili wytrzymać ze sobą .
Ralph obwiniał żonę , o to , że „ puściła rękę Jamesa , żyłby , gdyby tego nie zrobiła ” .
Z drugiej strony przyznawał też , że tego samego dnia rano żona prosiła go , by wziął Jamesa ze sobą do swojego szwagra .
Odmówił , argumentując , że zazwyczaj chętnie wszędzie go bierze , tym razem jednak ma w planie prace przy składaniu mebli z użyciem wielu narzędzi i boi się , że Jamesowi mogłaby niechcący stać się krzywda .
Ralph nie potrafił uporać się z myślą , że gdyby wówczas zabrał syna , nie zostałby on zabity .
Próbował zagłuszyć ból , pijąc whisky – najpierw jedną , potem nawet dwie butelki na dobę .
Nie przyniosło mu to ulgi , nie potrafił ani się upić , ani złagodzić bólu .
W 2013 roku zdobył się na przyznanie się do tego , że żonę oskarżał niesłusznie i jest mu z tego powodu wstyd .
Denise poznała później Stuarta Fergusa , wyszła za niego za mąż i ma z nim dwóch synów : Thomasa i Leona .
Ralph także zawarł nowy związek małżeński , jego wybranką została Natalie ( z domu McDermott ) , z którą ma córkę .
Denise Fergus prowadzi działalność społeczną i charytatywną pod nazwą James Bulger Memorial Trust , traktując to jako żywy pomnik dla Jamesa .
Ralph opisał swoje przeżycia w książce `` My James '' .
Odniesienia w kulturze .
W 2016 roku powstał film `` Plac zabaw '' w reżyserii Bartosza Kowalskiego inspirowany zabójstwem Jamesa Bulgera .
W 2018 roku , na podstawie nagrań z przesłuchań Jona Venablesa i Roberta Thompsa , powstał film Detainment w reżyserii Vincenta Lambego .
W 2019 roku został on nominowany do Oscara w kategorii Najlepszy krótkometrażowy film aktorski .
