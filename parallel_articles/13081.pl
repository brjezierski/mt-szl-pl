Afryka Zachodnia
Afryka Zachodnia – umowna nazwa określająca tereny położone w Afryce na północ od Zatoki Gwinejskiej i na południe od Sahary .
Obejmuje obszary zachodniego Sahelu , zachodniego Sudanu , a także częściowo tereny podrównikowych lasów deszczowych .
Geografia .
Organizacja Narodów Zjednoczonych do regionu Afryki Zachodniej zalicza następujące państwa :
Czasami do Afryki Zachodniej zalicza się także Saharę Zachodnią ( najczęściej jednak wliczana jest ona do krajów Maghrebu ) , Czad i Kamerun ( dwa ostatnie ONZ włącza do Afryki Środkowej ) .
Ze względu na swoje położenie na Saharze Mauretania jest często wyłączana spośród państw Afryki Zachodniej .
Warunki naturalne .
Przeważającą część terytorium Afryki Zachodniej zajmują równiny z wysokością bezwzględną nie przekraczającą 300 m n.p.m .
– wyżej położone tereny występują tylko lokalnie : najwyższe wzniesienie w Afryce Zachodniej to Bintumani w Sierra Leone ( 1948 m n.p.m . )
lub – jeśli uznać przynależność Kamerunu do tego regionu – Góra Kamerun ( 4095 m n.p.m . ) .
Położenie w obszarze zachodniego Sudanu sprawia , że region ten cechuje się klimatem półsuchym z przeważającą roślinnością charakterystyczną dla obszaru sawann z porą suchą przeważnie w okresie od listopada do marca .
