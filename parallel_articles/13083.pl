Zachodni Brzeg
Zachodni Brzeg ( arab .
الضفة الغربية , trb .
`` ad-Diffa al-Gharbija '' ; hebr .
הגדה המערבית , trb .
`` ha-Gada ha-Ma ’ arawit '' ) , zwany także Zachodnim Brzegiem Jordanu i Cisjordanią – część Palestyny , de facto kontrolowana przez Izrael i stanowiąca jego jednostkę administracyjną o nazwie Judea i Samaria na zachód od doliny rzeki Jordan .
Geografia .
Powierzchnia : 5800 km² .
Ludność : 2,3 miliona Palestyńczyków i 475 tysięcy Żydów zamieszkujących 141 kolonii zbudowanych z inicjatywy władz Izraela po wojnie sześciodniowej w 1967 roku .
Najważniejsze miasta : Jerycho , Betlejem , Dżanin , Nablus , Ramallah i Hebron .
Historia .
Obszar Zachodniego Brzegu Jordanu w latach 1920–1947 stanowił część brytyjskiego mandatu Palestyny .
W wyniku działań zbrojnych podczas pierwszej wojny izraelsko-arabskiej ( 1948 ) został włączony do Jordanii .
24 kwietnia 1950 roku Jordania ogłosiła jego aneksję , którą uznały Pakistan i Wielka Brytania .
Podczas wojny sześciodniowej ( 1967 ) został zajęty przez wojska izraelskie i odtąd jest okupowany przez Izrael .
W 1972 roku Jordania zaproponowała stworzenie federacji Zachodniego Brzegu i Jordanii , co zostało odrzucone przez Federację Państw Arabskich .
W 1988 roku król Husajn I formalnie zrzekł się praw do tego terytorium .
W 1994 roku Palestyńczycy z Jerycha i otaczającego je obszaru ( ok. 60 km² ) uzyskali autonomię ( w ramach palestyńskiej autonomii w Strefie Gazy i mieście Jerycho ) .
W 1995 roku nastąpiło rozszerzenie autonomii na dalsze obszary Zachodniego Brzegu Jordanu .
Na tych obszarach obecnie trwają zamieszki i walki ( intifada ) między nieregularnymi , często uznawanymi za terrorystyczne , organizacjami palestyńskimi a armią izraelską .
