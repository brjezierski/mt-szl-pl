Wirginia Zachodnia
Wirginia Zachodnia ( , ) – stan we wschodniej części Stanów Zjednoczonych .
Na północy graniczy z Pensylwanią i Maryland , od wschodu i południa z Wirginią , a na zachodzie z Kentucky i Ohio .
Stolicą stanu jest Charleston .
Wirginia Zachodnia , położona w regionie Appalachów , ma jedne z najbardziej nierównych terenów w kraju .
Faliste góry , wzgórza i doliny tego stanu sprawiły , że zyskał przydomek `` The Mountain State '' ( Górzysty Stan ) i jest dobrze znanym z szerokiej gamy zajęć na świeżym powietrzu , w tym turystyki pieszej , kolarstwa górskiego , narciarstwa i raftingu .
W latach 2010–2020 Wirginia Zachodnia odnotowała najwyższy procentowy spadek swoich mieszkańców wśród stanów USA .
Spadek populacji spowodowany jest głównie starzeniem się populacji i wzrostem bezrobocia spowodowanym zamykaniem kopalń .
Historia .
Kiedy Wirginia odłączyła się od Unii w 1861 roku , część znana obecnie jako Wirginia Zachodnia oderwała się , tworząc własny stan .
Przyjęta w roku 1863 Wirginia Zachodnia była jedynym stanem powstałym z oderwania się od stanu konfederackiego podczas wojny secesyjnej i odegrała kluczową rolę jako stan graniczny .
