Województwo zachodniopomorskie
Województwo zachodniopomorskie – województwo położone w północno-zachodniej części Polski , na wybrzeżu Morza Bałtyckiego .
Graniczy na wschodzie z województwem pomorskim , na południu z województwami : wielkopolskim i lubuskim , a na zachodzie z niemieckimi krajami związkowymi : Brandenburgią i Meklemburgią-Pomorzem Przednim .
Zajmuje obszar 22 892,48 km² , 31 grudnia 2021 r. miało około 1,68 mln mieszkańców .
Siedzibą władz województwa jest Szczecin .
Jest najdalej wysuniętym na zachód województwem w Polsce .
Województwo zostało powołane w roku 1999 w związku z reformą administracji publicznej , w miejsce dawnych województw : szczecińskiego i koszalińskiego , a także części województw : słupskiego , pilskiego i gorzowskiego .
Nazwą nawiązuje do historycznego Pomorza Zachodniego .
