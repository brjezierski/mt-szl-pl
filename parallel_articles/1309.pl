Arezzo
Arezzo – miasto i gmina we Włoszech , w regionie Toskania , stolica prowincji Arezzo .
Według danych na styczeń 2010 gminę zamieszkiwały 99 503 osoby przy gęstości zaludnienia 257,6 os./km² .
Arezzo jest także jedną z prowincji Toskanii o powierzchni 3232 km² , liczącą 318,8 tys .
mieszkańców i podzieloną na 39 gmin .
Historia .
Założone przez Etrusków , należało do Ligi Etruskiej , później kolonia rzymska .
W XI wieku wolna komuna , w opozycji do Florencji , toczyła z nią wieloletnie walki ( Campaldino 1289 ) .
