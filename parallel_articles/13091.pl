Zadar
Zadar ( wł .
`` Zara , '' , węg .
`` Zára '' , niem .
`` Jadera '' ) – miasto w Chorwacji , stolica żupanii zadarskiej , siedziba miasta Zadar .
Leży w Dalmacji , nad Morzem Adriatyckim .
W 2011 roku liczyło 71 471 mieszkańców .
Znaczenie .
Jest to centrum gospodarcze oraz kulturalne północnej Dalmacji i jednocześnie piąte co do wielkości miasto kraju .
Stolica administracyjna żupanii zadarskiej .
Ekonomia .
Znajduje się tu port handlowy i rybacki , stocznia oraz port lotniczy .
Zadar jest ważnym ośrodkiem przemysłowym ( przemysł spożywczy , chemiczny i włókienniczy ) , kulturalnym ( liczne zabytki i muzea ) i akademickim ( Uniwersytet w Zadarze ) .
Historia .
Historia miasta sięga IV w. p.n.e .
Na terenie dzisiejszego Zadaru istniała osada iliryjskiego plemienia Liburnów .
300 lat później miasto , nazywane wówczas Jadrea , znalazło się pod panowaniem rzymskim .
Za czasów panowania Cezara i Augusta umocniono je , otaczając murami obronnymi z basztami i trzema bramami , wytyczono forum , wzniesiono świątynie i bazyliki , amfiteatr , a także akwedukt i cmentarz .
