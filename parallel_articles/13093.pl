Zagharta
Zagharta ( Zgharta , Zghorta , Zaghorta ; arab .
زغرتا ) – miasto w północnym Libanie , siedziba dystryktu , 7 km od Trypolisu , 91 km od Bejrutu .
Administracyjnie tworzy jedną jednostkę municypalną z miastem Ehden , oddalonym o 24 km .
W czasie libańskiej wojny domowej Zagharta była bastionem chrześcijańskiej milicji Brygada Marada .
