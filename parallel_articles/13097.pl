Zagórz ( stacja kolejowa )
Zagórz – stacja kolejowa w Zagórzu .
Przy stacji działa całodobowo posterunek Straży Ochrony Kolei .
W roku 2017 stacja obsługiwała 10–19 pasażerów na dobę .
Historia .
.
Budowa kolei przez Zagórz miała na celu połączenie Wiednia i Budapesztu z galicyjską twierdzą Przemyśl .
Zagórz w tamtym czasie był maleńką i biedną wioską .
Cały odcinek biegnący ze Słowacji przez Komańczę , Zagórz , Chyrów na Ukrainie do Przemyśla stał się przejezdny 25 grudnia 1872 .
Linia kolejowa Jasło – Zagórz jest o 12 lat młodsza od pozostałych .
Poszczególne odcinki linii oddawano do eksploatacji w następujących terminach : Przemyśl-Chyrów ; 33,2 km – 13 maja 1872 .
Chyrów-Krościenko ; 19,4 km – 1 lipca 1872 .
