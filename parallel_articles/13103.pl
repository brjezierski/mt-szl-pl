Obwód zakarpacki
Obwód zakarpacki ( ) – jeden z 24 obwodów Ukrainy .
Leży w zachodniej części Ukrainy , na obszarze historycznego Zakarpacia .
Graniczy z Polską , Słowacją , Węgrami i Rumunią .
Stolicą obwodu jest Użhorod .
Obwód został utworzony 22 stycznia 1946 roku .
Obwód zakarpacki graniczy od północy z polskim województwem podkarpackim oraz z obwodami lwowskim i iwanofrankowskim , od południa – z rumuńskimi okręgami Marmarosz i Satu Mare , od południowego zachodu – z węgierskim komitatem Szabolcs-Szatmár-Bereg , od zachodu – ze słowackimi krajami preszowskim i koszyckim ( historyczny region Zemplín ) .
Jest jedynym obwodem Ukrainy który graniczy bezpośrednio z czterema państwami .
Gospodarka .
W obwodzie rozwinął się przemysł drzewno-papierniczy , spożywczy oraz lekki .
W regionie wydobywa się węgiel brunatny oraz sól kamienną .
W obwodzie zakarpackim uprawia się zboże , tytoń , drzewa owocowe , winorośl oraz hoduje się bydło , trzodę chlewną , owce .
Historia .
Obwód zakarpacki został utworzony 22 stycznia 1946 r. dekretem Prezydium Rady Najwyższej ZSRR .
Jak nazwa wskazuje , jest jedyną częścią Ukrainy leżącą po południowej stronie Karpat .
