Zakopane ( stacja kolejowa )
Zakopane – stacja kolejowa w Zakopanem , w województwie małopolskim , w Polsce .
Stacja czołowa i koniec linii nr 99 .
Historia .
Fundatorem i organizatorem budowy kolei do Zakopanego był Władysław Zamoyski , którego upamiętnia tablica pamiątkowa na dworcu , ustanowiona w 1999 w 100-lecie fundacji .
Projektantem rozbudowy budynku dworca , przeprowadzonej w latach 1937–1939 , z uwagi na odbywające się w mieście Mistrzostwa Świata w Narciarstwie Klasycznym 1939 , był inż .
Zenon Thienel .
W czerwcu 2022 dotychczasowy układ torowy stacji rozebrano , a w jego miejsce do połowy 2023 ma powstać nowy układ torów i peronów , przy czym niweleta torowiska zostanie obniżona o 1 metr .
