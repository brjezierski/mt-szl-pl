Zakroczym ( gmina )
Zakroczym – gmina miejsko-wiejska w województwie mazowieckim , w powiecie nowodworskim .
W latach 1975–1998 gmina położona była w województwie warszawskim .
Siedziba gminy to Zakroczym .
Według danych z 30 czerwca 2004 gminę zamieszkiwało 6385 osób .
Natomiast według danych z 31 grudnia 2017 roku gminę zamieszkiwało 6087 osób .
