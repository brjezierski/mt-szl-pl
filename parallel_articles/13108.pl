Zakręcie ( gromada )
Zakręcie ( alt .
gromada `` Zakręcie kol . '' )
– dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Zakręcie z siedzibą GRN w Zakręciu utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie krasnostawskim w woj .
lubelskim na mocy uchwały nr 9 WRN w Lublinie z dnia 5 października 1954 .
W skład jednostki wszedł obszar dotychczasowej gromady Jaślików ze zniesionej gminy Krasnystaw oraz miejscowość Zakręcie kol .
wyłączona z miasta Krasnystaw w tymże powiecie .
Dla gromady ustalono 13 członków gromadzkiej rady narodowej .
1 stycznia 1960 do gromady Zakręcie kol .
włączono obszar zniesionej gromady Stężyca Łęczyńska w tymże powiecie .
1 stycznia 1969 gromadę zniesiono , włączając jej obszar do gromady Krasnystaw w tymże powiecie .
