Zalaegerszeg
Zalaegerszeg – miasto na Węgrzech , w południowo-zachodniej części Kraju Zadunajskiego , stolica komitatu Zala .
Leży po obu stronach rzeki Zala .
Stanowi ośrodek geograficzno-etnograficznego subregionu Göcsej ( Közép-Zalai-dombság ) .
Liczba ludności ok. 62 tys .
( styczeń 2011 r. ) .
Mimo wczesnego osadnictwa na tych terenach , poświadczanego przez znaleziska archeologiczne oraz późniejsze pisemne wzmianki , Zalaegerszeg jest miastem ukształtowanym dopiero w XIX i XX w. Od XV w. miejscowość znajdowała się w posiadaniu Kościoła rzymskokatolickiego .
W 1885 r. otrzymała prawa miejskie , a w 1890 r. doprowadzono do niej linię kolejową .
Rozwój na szerszą skalę nastąpił po II wojnie światowej wraz z odkryciem w okolicy złóż ropy naftowej ( 1952 ) .
