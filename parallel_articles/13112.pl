Zaleszczyki ( stacja kolejowa )
Zaleszczyki ( ukr .
Заліщики ) – stacja kolejowa w Zaleszczykach , w rejonie zaleszczyckim , w obwodzie tarnopolskim , na Ukrainie na linii Biała Czortkowska – Stefaneszty .
Zarządzana przez Kolej Lwowską .
Historia .
W II Rzeczypospolitej była stacją graniczną na granicy z Rumunią z obsługą ruchu transgranicznego .
Stacją graniczną po stronie rumuńskiej był Schit .
Ponieważ Zaleszczyki w tym okresie były popularnym kurortem letnim , kursował do nich bezpośredni „ sleeping ” z Warszawy .
Połączenie Gdynia – Zaleszczyki było najdłuższą trasą kolejową w kraju .
Z Zaleszczyk wywożono uprawiane tu egzotyczne , jak na polskie warunki , owoce – arbuzy , brzoskwinie , melony oraz winogrona .
Po II wojnie światowej stacja straciła swój nadgraniczny charakter .
Wtedy również upadła turystyka w mieście .
