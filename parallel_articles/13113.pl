Zaliwie-Brzozówka
Zaliwie-Brzozówka – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Jadwigi w Mokobodach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Posiada 40 mieszkańców .
Miejscowość powstała początkowo jako kolonia wsi Zaliwie Piegawki w wyniku komasacji gruntów w latach '20 XX wieku oraz przeniesienia się części gospodarzy wsi Zaliwie Brzozówka na grunty zwane `` Brzozówka '' , znajdujące się kilometr od Zaliwia Piegawek w stronę miejscowości Kwaśnianka .
W czasach PRL utworzono oddzielną miejscowość Zaliwie Brzozówka , należącą do gminy Mokobody .
