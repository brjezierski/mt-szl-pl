Zaliwie-Piegawki
Zaliwie-Piegawki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Jadwigi w Mokobodach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Pierwsza wzmianka o Zaliwiu pojawiła się w dokumentach z 1476 roku .
W 1827 roku wieś Zaliwie Piegawki ( albo `` Biegawki '' ) liczyła 17 domów zamieszkałych przez 110 mieszkańców .
U schyłku XIX w. liczba mieszkańców liczyła 126 osób , utrzymujących się z uprawy 595 mórg ziemi ( 333 ha ) .
Wieś Zaliwie Piegawki ( pod nazwą Zaliwie ) została umieszczona na mapie Zachodniej Galicji z 1770 roku .
Do końca XIX w. istniał tu niewielki dwór oraz karczma dworska , a także drewniany most na rzece Liwiec łączący Zaliwie z miasteczkiem Mokobody i młyn wodny .
Przy moście od strony Mokobód był folwark Ostaszewizna .
Most i młyn spłonęły w pożarze podczas I wojny światowej .
Most został odbudowany ok. 150 metrów w stronę wsi Zaliwie Szpinki , w którym to miejscu stoi do dzisiaj .
W trakcie II wojny światowej drewniany most został wysadzony przez Niemców w 1944 r. i odbudowany w tym samym roku jako drewniany przez Rosjan za pomocą drewna uzyskanego z rozbiórki budynków położonych przy moście .
Miejscowość do 1947 roku należała do parafii Niwiski następnie została przyłączona do parafii Mokobody .
