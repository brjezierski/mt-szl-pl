Zaliwie-Szpinki
Zaliwie-Szpinki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody nad rzeką Liwiec .
Pierwsza wzmanka o Zaliwiu pojawiła się dnia 19 czerwca 1419 roku .
Tego dnia książę mazowiecki Janusz I Starszy na wniosek Zdzisława z Dąbrówki , Pawła i Zdzisława z Zaliwia formalnie potwierdza ich prawa własności do tych dóbr , jednocześnie ustanawiając podatek 12 groszy od włóki oraz prawo do połowu bobrów .
W roku 1476 majątek Zaliwie dzieli się między dwóch szlichciców Jana Wspinka i Jana Piegawkę i od ich przydomków biorą się nazwy Zaliwie Szpinki i Zaliwie-Piegawki .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Jadwigi w Mokobodach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W Zaliwiu-Szpinkach , przy moście na drodze łączącej Zaliwie z Mokobodami rozpoczyna się trasa , spływu kajakowego Liwcem .
Trasa ma długości 15,7 km .
Spływ trwa około czterech godziny i kończy się we wsi Wyszków .
Na miejscu jest możliwość wypożyczenia kajaków , a także możliwość transportu osób i sprzętu do miejsca startu .
W roku 2014 ze środków Europejskiego Funduszu Rozwoju Regionalnego oraz budżetu zostały oznakowane szlaki kajakowe na Liwcu i dopływach Kostrzyniu i Muchawce .
