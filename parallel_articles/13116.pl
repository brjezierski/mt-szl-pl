Zamarski ( cz .
Zámrsk , niem .
Zamarsk ) – wieś w Polsce , położona w województwie śląskim , w powiecie cieszyńskim , w gminie Hażlach , w granicach historycznego regionu Śląska Cieszyńskiego , geograficznie zaś na Pogórzu Śląskim .
Na obszarze 864,22 ha zamieszkana jest przez 1404 mieszkańców ( 2018 ) , co daje gęstość zaludnienia równą 162,5 os./km² .
