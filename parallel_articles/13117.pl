Zambia
Zambia , Republika Zambii ( ang .
`` Republic of Zambia '' ) , dawniej Rodezja Północna – państwo w południowej Afryce bez dostępu do morza .
Nazwa przyjęta od Zambezi – głównej rzeki kraju .
Geografia .
Największe miasta : Lusaka , Kitwe-Nkana , Ndola , Mufulira , Chingola , Livingstone , Chipata , Kapiri-Mposhi , Solwezi .
Ukształtowanie powierzchni .
Obszar Zambii obejmuje lekko pofałdowany płaskowyż ( 1000–1400 m n.p.m .
) , który stanowi znaczną część lewobrzeżnego dorzecza Zambezi .
Na południu znajduje się rozległa Dolina Luangwy , z jednej strony ograniczona przez pasmo górskie Muczinga .
Najwyższym szczytem jest Mafinga w paśmie Mafinga Hills o wysokości 2329 m n.p.m .
Historia .
Pierwotnie tereny dzisiejszej Zambii zamieszkiwali Buszmeni , a od ok. VI wieku zaczął się napływ ludów Bantu .
Od XIV do XV wieku pod wpływami państwa Monomotapa .
W XIX wieku na terenie Zambii powstały dwa stabilne twory państwowe – państwo Barotse ( na zachodzie ) i Bemba ( na południowym zachodzie ) .
W 1835 w Zambii osiedlił się zuluski lud Nguni .
W II połowie XIX wieku tereny Zambii zaczęły być stopniowo penetrowane przez europejskich misjonarzy , handlarzy i odkrywców .
Jednym z odkrywców był Brytyjczyk David Livingstone .
On jako pierwszy z Europejczyków zobaczył wodospad na rzece Zambezi i nazwał go na cześć królowej Wiktorii .
W 1888 Cecil Rhodes uzyskał od miejscowych władców przywileje do eksploatacji złóż mineralnych .
Od 1902 Zambia znalazła się pod zarządem Brytyjskiej Kompanii Afryki Południowej , którą kierował Rhodes .
W tym czasie rozpoczęła się tam eksploatacja bogatych złóż rud miedzi , a także budowa linii kolejowych dla umożliwienia wywozu surowców .
W 1911 roku tereny te zostały przekształcone w brytyjski protektorat o nazwie Rodezja Północna .
Od 1924 do uzyskania niepodległości Rodezją Północną administrował rząd brytyjski .
