Zamość ( Wronki )
Zamość – osiedle położone w północnej części Wronek .
Największe prawobrzeżne osiedle Wronek , przebiega przez nie droga wojewódzka nr 150 ( ulica Myśliwska ) .
W południowej części Zamościa znajduje się zbiornik wodny Cegielnia , pozostałość po wyrobisku gliny .
W centralnej części osiedla znajduje się skwer imienia Włodzimierza Krzeszkiewicza , leśnika urodzonego we Wronkach , od strony ulicy Jaworowej stoi figura Chrystusa Króla .
W północnej części osiedla ulokowano cmentarz komunalny ( ul .
Rzecińska ) , zaś we wschodniej stadion drużyny piłkarskiej Amica Wronki .
