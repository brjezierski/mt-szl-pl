Produkcja dawniej .
Tradycyjnie śmietanę wytwarzano z surowego , świeżo wydojonego mleka .
Wlewano je do glinianych naczyń i pozostawiano w temperaturze pokojowej .
Po kilku godzinach na powierzchni mleka pojawiała się śmietanka .
Po kilku dniach , w wyniku fermentacji mlekowej , powstawała właściwa kwaśna śmietana .
Używano także glinianych lub metalowych odstojników z otworami na dole .
