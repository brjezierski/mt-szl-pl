Zapopan
Zapopan – miasto w Meksyku , w stanie Jalisco , w aglomeracji Guadalajary , na północny zachód od tego miasta .
Przez Zapopan płynie rzeka Río Atemajac .
W 2014 roku miasto liczyło 1 202,9 tysięcy mieszkańców .
W mieście rozwinął się przemysł elektroniczny , tytoniowy , mineralny oraz chemiczny .
