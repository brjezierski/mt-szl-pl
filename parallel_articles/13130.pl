Saragossa
Saragossa ( ) – miasto w północno-wschodniej Hiszpanii , w środkowym biegu Ebro .
Stolica Aragonii , znana z mauretańskiego pałacu i barokowej katedry .
Etymologia .
Obecna nazwa miasta pochodzi od łacińskiego `` Caesaraugusta '' ; po zdobyciu miasta w VIII wieku Maurowie przekształcili ją na `` Medina Albaida Sarakosta '' , a Hiszpanie po odbiciu miasta zmodyfikowali na `` Zaragoza '' ( w języku kastylijskim i aragońskim ) .
Demografia .
Według danych hiszpańskiego Ministerstwa Rozwoju tzw .
`` Wielki Zespół Miejski '' Zaragozy ( hiszp .
`` Grande Área Urbana '' ) ma 746 152 mieszkańców na powierzchni 2295 km² , w latach 2001–2011 nastąpił wzrost ludności o 95 031 osób , co stanowi wzrost o 14.6 % .
