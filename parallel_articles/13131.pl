Zaria ( rejon rylski )
Zaria ( ) – osiedle typu wiejskiego w zachodniej Rosji , w sielsowiecie nikolnikowskim rejonu rylskiego w obwodzie kurskim .
Geografia .
Miejscowość położona jest 4,5 km od centrum administracyjnego sielsowietu nikolnikowskiego ( Makiejewo ) , 14 km od centrum administracyjnego rejonu ( Rylsk ) , 110 km od Kurska .
Demografia .
W 2010 r. miejscowość nie posiadała mieszkańców .
