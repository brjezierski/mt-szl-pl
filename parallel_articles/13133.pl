Zasmidium cellare
Zasmidium cellare ( Pers . )
Fr .
– gatunek grzybów z rodziny `` Mycosphaerellaceae '' .
Występuje w różnych środowiskach , m.in .
w piwnicach i magazynach na wino , piwo i brandy .
Notowany był również w Polsce .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Zasmidium , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1794 r. Christiaan Hendrik Persoon nadając mu nazwę `` Racodium cellare '' .
Elias Fries w 1849 r. przeniósł go do rodzaju `` Zasmidium '' .
