Zatoka Kolska
Zatoka Kolska ( ros .
Кольский залив , `` Kolskij zaliw '' ) – przypominająca fiord zatoka Morza Barentsa , u północnych brzegów należącego do Rosji Półwyspu Kolskiego .
Wcina się w ląd na długość ok. 57 km .
U wejścia zatoka ma szerokość 7 km i zwęża się ku południu osiągając ok. 1 km szerokości .
Głębokość u wejścia wynosi ok. 200-300 m. Do zatoki wpadają rzeki Tułoma i Koła .
Wschodni brzeg jest górzysty i stromy , zachodni zaś stosunkowo płaski .
Po wschodniej stronie leżą porty Murmańsk i Siewieromorsk .
Po zachodniej stronie znajduje się Polarnyj , główna baza rosyjskiej Floty Północnej .
Pływy w zatoce osiągają 4 m. W zimie w południowej części zatoka bywa pokryta lodem .
