Zawichost ( gmina 1888–1925 )
Zawichost ( do 1888 i od 1926 `` miasto Zawichost '' ) – dawna gmina wiejska istniejąca do 1924 roku w woj .
kieleckim .
Siedzibą władz gminy była osada miejska Zawichost .
Gmina Zawichost powstała 26 lipca 1888 roku w powiecie sandomierskim w guberni radomskiej w związku z utratą praw miejskich przez miasto Zawichost i przekształceniu jego w wiejską gminę Zawichost w granicach dotychczasowego miasta .
W okresie międzywojennym gmina należała do powiatu sandomierskiego w woj .
