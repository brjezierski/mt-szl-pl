Zawiść ( Orzesze )
Zawiść – dzielnica Orzesza .
Historia .
Pierwsza udokumentowana informacja o Zawiści , wiosce rycerskiej pochodzi z 1574 r. , kiedy Wawrzyniec Trach odkupił wieś od Jerzego Orzeskiego , chociaż można również dopuścić , że wymieniona przez Jana Raciborskiego wśród wiosek przekazywanych żonie , księżnej Helenie nazwa `` Czawsch '' , dotyczy Zawiści i wówczas należałoby czas powstania wioski cofnąć co najmniej o sto lat .
Następnym wzmiankowanym panem Zawiści był Krzysztof Fragstein ( 1653 ) .
W 1715 r. właścicielem był Karol Aleksander Holly , a potem Zawadzcy , Właściciele Gardawic .
Od 1852 r. wioskę posiadali po kolei : Witowski , Micke , Hegenscheid i poprzez małżeństwo Fryderyk Penner .
Ten ostatni władał Zawiścią do zakończenia I wojny światowej .
W 1921 r. majątek został rozparcelowany .
W 1921 r. podczas plebiscytu 199 osób głosowało za Polską , a 99 za Niemcami .
W 1780 r. zarejestrowano w Zawiści 5 gospodarstw i 50 mieszkańców , a w 1891 r. żyło już tutaj 500 ludzi .
W 1896 r. Przygotowano pomieszczenia na szkołę i od tego czasu Zawiść oddzielono od szkoły w Gardawicach .
Budynek z cegły , kryty dachówką powstał w 1907 r. Po wojnie w 1965 r. postawiono drugą , obszerną szkołę , która służy do dzisiaj .
W Zawiści istniała od 1733 do 1914 r. huta szkła `` Ernestyna '' .
Również w pierwszej połowie XVIII w. wybudowano pałac , a nieco później obok zabudowania gospodarcze folwarku .
W tym czasie działały w wiosce 2 cegielnie i jedna kopalnia .
