Załoźce
Załoźce ( ukr .
Залізці ) – osiedle typu miejskiego w obwodzie tarnopolskim , w rejonie zborowskim Ukrainy , leżące nad Seretem .
Nazwa , geografia .
Do 1919 roku również w zapisach jako Załośce , Załośce Stare i Nowe .
Leżą nad Seretem , który rozdziela miejscowość na Załoźce Stare i Załoźce Nowe .
Historia .
Pierwsza wzmianka o Załoźcach pochodzi z 1441 roku , wymieniane jako miasto w 1511 roku lub 1516 .
W tym czasie Marcin Kamieniecki rozpoczął budowę lub zbudował tu zamek , który pod koniec XVI wieku był własnością Konstantego Wiśniowieckiego .
Parafia rzymskokatolicka w mieście przez pewny czas wchodziła w skład diecezji łuckiej , chociaż kanonik katedry lwowskiej ks .
Tomasz Pirawski w swym dziele „ Relatio status almae Archidioecesis Leopoliensis ” „ napisanym w latach 1614 i 1615 twierdził , że kościoły w Olesku , Załoźcach i Brodach należeć powinny do dziekanii złoczowskiej archidiecezji lwowskiej .
Przy końcu XVIII wieku hrabia Ignacy Miączyński ufundował w mieście klasztor i szpital Sióstr Miłosierdzia .
W wyniku I rozbioru Polski Załoźce znalazły się pod władzą Austrii .
W tym czasie utraciły prawa miejskie .
W czasie I wojny światowej w okolicy miasta toczyły się ciężkie boje oddziałów austriackich z wojskami rosyjskimi , w jednych i drugich licznie służyli Polacy .
