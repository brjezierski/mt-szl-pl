Argentyna ( Zawiercie )
Argentyna – osiedle Zawiercia , znajdujące się w południowo-wschodniej części miasta , rozciągające się od Warty po teren CMC Zawiercie .
Jest osiedlem o charakterze wielorodzinnym .
W roku 2015 liczyło 2744 mieszkańców .
Historia .
Osiedle ( wówczas dzielnica ) powstało przed I wojną światową .
Funkcjonowała wówczas na nim przędzalnia Wojca i S-ki .
W dwudziestoleciu międzywojennym osiedle zostało zurbanizowane i nacechowane gęstą siatką ulic .
Argentyna była wówczas jednym z niewielu osiedli Zawiercia utworzonym według określonego planu architektonicznego .
Wskutek kryzysu przełomu lat 20. i 30 .
Argentyna stała się zaniedbanym osiedlem biedoty .
W 1936 roku na terenie osiedla zbudowano stadion Warty Zawiercie .
W 1972 roku na terenie dawnego stadionu powstała Spółdzielnia Inwalidów „ Warta ” .
W zakładach tych osoby niepełnosprawne produkowały elementy elektroniczne i motoryzacyjne .
Spółdzielnia została postawiona w stan likwidacji w 2000 roku .
W 1986 roku przy ul .
Polskiej otwarto miejską ciepłownię .
W tym okresie rozpoczęto również dużą inwestycję mieszkaniową na terenie osiedla , a także zaadaptowano teren po stawie „ Lodownia ” jako zajezdnię ZKM Zawiercie .
W roku 1989 na terenie osiedla erygowano rzymskokatolicką parafię pw .
Najświętszego Ciała i Krwi Chrystusa .
Kościół parafialny budowano w latach 1995–1998 .
W 2010 pojawił się plan włączenia Argentyny do dzielnicy Żabki , ale nie został on zrealizowany .
Ulice .
Do osiedla należą następujące ulice : Amatorska , Brzechwy , Kijowska , Kraszewskiego , Krakowska , Metalowców , Mylna , Polska , Przechodnia , Rataja , Równa , Senatorska , Sienkiewicza , Stalowa , Tuwima , Warszawska , Wenecka , Zaporowska , Zielna , Ziemna , Zimna .
