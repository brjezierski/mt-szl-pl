Zbaraż ( stacja kolejowa )
Zbaraż ( , ) – stacja kolejowa w miejscowości Zbaraż , w rejonie tarnopolskim , w obwodzie tarnopolskim , na Ukrainie .
Położona jest na linii Szepetówka – Tarnopol .
Stacja powstała w czasach Austro-Węgier .
Przed I wojną światową była stacją końcową linii od strony Tarnopola .
