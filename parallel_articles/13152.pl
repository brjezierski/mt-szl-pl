Zbigniew Krzyżoś
Zbigniew Krzyżoś ( ur .
17 lutego 1959 we Wrocławiu ) – polski piłkarz grający na pozycji pomocnika .
Zdobywca Pucharu Polski w 1986 roku z GKS-em Katowice .
Życiorys .
W czasie swojej kariery zawodniczej mierzył 177 cm i ważył 72 kg .
Piłkę nożną zaczął trenować w Górniku Siersza .
W 1979–1987 był zawodnikiem GKS-u Katowice .
W sezonie 1985/1986 z katowickim zespołem wywalczył Puchar Polski .
Wówczas rozegrał 24 mecze ligowe , w których strzelił jedną bramkę .
Później , nadal uczestnicząc w I lidze , reprezentował Szombierki Bytom i Wisłę Kraków .
W 1990 roku , po półrocznej przerwie gry w piłkę , wyjechał do Niemiec , gdzie do końca swojej kariery grał w TuS Eisenbach .
