Zbigniew Religa
Zbigniew Eugeniusz Religa ( ur .
16 grudnia 1938 w Miedniewicach , zm .
8 marca 2009 w Warszawie ) – polski kardiochirurg i polityk , profesor nauk medycznych .
Senator III ( 1993–1997 ) i V kadencji ( 2001–2005 ) .
W latach 2005–2007 minister zdrowia w rządzie Kazimierza Marcinkiewicza , a następnie w rządzie Jarosława Kaczyńskiego .
Od 2007 do 2009 poseł na Sejm VI kadencji .
Członek Międzynarodowej Kapituły Orderu Uśmiechu .
Kawaler i członek Kapituły Orderu Orła Białego , patron podstawowego programu Fundacji Dzieciom „ Zdążyć z Pomocą ” ( od 1998 ) .
Życiorys .
Wykształcenie i kariera zawodowa .
W 1956 uzyskał świadectwo dojrzałości w Liceum Ogólnokształcącym im .
Bolesława Limanowskiego w Warszawie .
Rozważał studia filozoficzne lub dziennikarskie , ostatecznie rozpoczął studia na Wydziale Lekarskim Akademii Medycznej w Warszawie .
Po ich ukończeniu w latach 1963–1964 odbył staż podyplomowy , a następnie służbę wojskową ( 1964–1966 ) .
W 1966 rozpoczął pracę w Szpitalu Wolskim w Warszawie , uzyskując w tym czasie specjalizację I i II stopnia z chirurgii .
Pracował tam do 1980 , pełnił m.in .
funkcję zastępcy ordynatora .
Jego pierwszą samodzielną operacją było wycięcie wyrostka robaczkowego .
Po zdaniu egzaminów Educational Commission for Foreign Medical Graduates ( ECFMG ) uzyskał stypendium w Mercy Hospital w stanie Nowy Jork , po czym został oddelegowany na odbycie staży podyplomowych w Stanach Zjednoczonych : w 1973 z chirurgii naczyniowej oraz w 1975 z kardiochirurgii w Sinai Hospital w Detroit .
W 1973 uzyskał stopień doktora nauk medycznych , a w 1981 doktora habilitowanego .
W latach 1980–1984 pracował w Klinice Kardiochirurgii Instytutu Kardiologii w Warszawie kierowanej przez Wacława Sitkowskiego .
Był kierownikiem oddziału i zastępcą kierownika kliniki .
Od 1984 kierował Kliniką Kardiochirurgii Wojewódzkiego Ośrodka Kardiologii w Zabrzu .
W latach 1989–1991 był ordynatorem oddziału w Centralnym Szpitala Klinicznym MSW w Warszawie .
W latach 1998–2000 stał na czele Kliniki Kardiochirurgii w tej placówce .
W 1990 został profesorem Śląskiej Akademii Medycznej , a w 1995 prezydent RP Lech Wałęsa nadał mu tytuł naukowy profesora nauk medycznych .
