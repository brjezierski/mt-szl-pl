Zbigniew Rokita
Zbigniew Rokita ( ur .
4 kwietnia 1989 w Gliwicach ) – polski pisarz , dziennikarz i reporter .
Specjalizuje się w problematyce Europy Środkowej , Wschodniej oraz Górnego Śląska .
Publikował m.in .
w „ Polityce ” , „ Tygodniku Powszechnym ” , „ Znaku ” „ Dzienniku Gazecie Prawnej ” .
W latach 2012–2018 redaktor dwumiesięcznika „ Nowa Europa Wschodnia ” .
Ukończył studia na Uniwersytecie Jagiellońskim .
Laureat VIII edycji konkursu o Stypendium im .
Leopolda Ungera ( 2020 ) .
Kariera .
W 2021 roku Zbigniew Rokita został laureatem Nagrody Literackiej Nike za książkę `` Kajś .
Opowieść o Górnym Śląsku '' .
Autor otrzymał nagrodę podwójnie – decyzją jury oraz czytelników .
W laudacji autorstwa Tadeusza Nyczka , członka kapituły Nagrody Literackiej Nike , wybór reportażu Rokity uzasadniono : „ `` Kajś '' dotyczy sprawy zupełnie fundamentalnej dla naszej historii , o której świadomość powinna być powszechna , odkrywa zatopioną Atlantydę , zatopioną pośrodku Europy ” .
Jego książka „ Królowie strzelców ” została przetłumaczona na język ukraiński ( tłum .
Ołeksij Czupa ) .
Została także wystawiona w Teatrze Nowym w Zabrzu ( reż .
Piotr Ratajczak , scen .
Piotr Rowicki ) .
W 2021 roku zadebiutował także jako twórca teatralny – w Teatrze Zagłębia w Sosnowcu odbyła się prapremiera jego sztuki `` Nikaj '' w reżyserii Roberta Talarczyka .
Życie prywatne .
Jest kibicem Piasta Gliwice .
