Zbigniew Stonoga
Zbigniew Ryszard Stonoga ( ur .
17 października 1974 w Ozimku ) – polski przedsiębiorca , wideobloger , a także działacz polityczny , sygnalista odpowiedzialny za publikację akt śledztwa w sprawie afery podsłuchowej w Polsce ( 2014 ) .
Życiorys .
Ukończył technikum elektromechaniczne , z wykształcenia jest technikiem elektronikiem .
Deklarował , że studiował prawo na Wydziale Prawa i Administracji Uniwersytetu Warszawskiego , lecz studiów tych nie ukończył , jednak administracja UW zaprzeczyła , jakoby Zbigniew Stonoga był kiedykolwiek studentem tej uczelni .
Twierdził również , że uzyskał tytuł inżyniera na uczelni w Opolu .
W 2006 został skazany na 3,5 roku , a w 2015 na 1 rok bezwzględnego pozbawienia wolności .
W lutym 2015 założył tabloid „ Gazeta Stonoga ” .
W 2016 sąd w Pruszkowie uznał biznesmena za winnego pomówienia prokuratora .
Skazał go na 10 miesięcy ograniczenia wolności w postaci 40 godzin miesięcznie prac społecznych , a także nakazał mu wypłacenie nawiązki na rzecz pokrzywdzonego w wysokości 20 tys .
