W tym samym roku został prawomocnie skazany przez Sąd Rejonowy Warszawa Praga-Północ na rok bezwzględnego więzienia za oszustwo przy sprzedaży samochodu marki Lexus w 2009 .
Wnoszone przez niego odwołania zostały rozpatrzone przez sąd na jego niekorzyść .
W lutym 2017 miał stawić się w zakładzie karnym do odbycia wyroku , czego nie zrobił .
