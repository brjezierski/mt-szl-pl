Zbigniew Wodecki ( album )
Zbigniew Wodecki – debiutancki album studyjny Zbigniewa Wodeckiego wydany w 1976 .
Materiał na album nagrywano od 27 lutego do 29 marca 1976 w studiu nagraniowym Polskich Nagrań „ Muza ” .
Na albumie umieszczono 11 piosenek , w tym m.in .
„ Rzuć to wszystko co złe ” , „ Panny mego dziadka ” , „ Posłuchaj mnie spokojnie ” , „ Kochałem panią kilka chwil ” i „ Partyjka ” .
Wodeckiemu towarzyszyła orkiestra pod dyrekcją Wojciecha Trzcińskiego oraz zespół Alibabki .
Zdjęcie okładkowe wykonał Zbigniew Łagocki .
30 czerwca 2017 ukazała się zremasterowana wersja płyty , wzbogacona o niepublikowany utwór „ Tak daleko stąd , tak blisko ” .
