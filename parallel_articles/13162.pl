Zbuczyn
Zbuczyn ( do 31 grudnia 2002 `` Zbuczyn Poduchowny '' ) – wieś ( dawniej miasto ) w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Zbuczyn .
Leży nad rzeką Zbuczynką , 16 km od Siedlec , przy międzynarodowej trasie .
Pierwsza informacja na temat wsi Zbuczyn pochodzi z roku 1418 .
Przed 1720 rokiem Zbuczyn uzyskał lokację miejską .
Prawa miejskie utracił po 1779 roku .
Do schyłku XVIII wieku było to miasto królewskie w starostwie zbuczyńskim w ziemi łukowskiej województwa lubelskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
1 stycznia 2003 nastąpiła zmiana nazwy wsi ze `` Zbuczyn Poduchowny '' na `` Zbuczyn '' .
