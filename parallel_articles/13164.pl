`` Sbichau '' , następnie `` Weihersfelde '' ) – wieś kaszubska w Polsce położona w województwie pomorskim , w powiecie wejherowskim , w gminie Wejherowo .
Leży na obszarze leśnym Trójmiejskiego Parku Krajobrazowego .
W kierunku północno-zachodnim od Zbychowa znajduje się Jezioro Wyspowo .
Wieś jest siedzibą sołectwa `` Zbychowo '' w którego skład wchodzi również osada Wyspowo .
Wieś szlachecka położona była w II połowie XVI wieku w powiecie puckim województwa pomorskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Podczas zaboru pruskiego wieś nosiła nazwę niemiecką `` Sbichau '' .
Podczas okupacji niemieckiej nazwa `` Sbichau '' w 1942 została przez nazistowskich propagandystów niemieckich ( w ramach szerokiej akcji `` odkaszubiania i odpolszczania nazw niemieckiego lebensraumu '' ) zweryfikowana jako zbyt `` kaszubska '' i przemianowana na `` nowo wymyśloną i bardziej niemiecką '' – `` Weihersfelde '' .
