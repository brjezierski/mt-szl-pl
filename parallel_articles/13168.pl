Zdeněk Miler
Zdeněk Miler ( ur .
21 lutego 1921 w Kladnie , zm .
30 listopada 2011 w Novej Vsi pod Pleší ) – czeski reżyser , scenarzysta i rysownik , twórca filmów animowanych .
Twórca m.in .
Krecika – bohatera kreskówek dla dzieci .
Życiorys .
Studiował w wyższej szkole artystycznoprzemysłowej w Pradze .
Początkowo zamierzał jedynie malować obrazy , od roku 1942 zaczął pracować jako animator w studiach filmowych w Zlinie .
W roku 1945 przeszedł do studia Bratři v triku , kierowanego przez Jiříego Trnkę .
Pracował jako animator , scenarzysta i reżyser artystyczny , a także zaczął pisać opowiadania .
Pierwszym jego samodzielnym filmem był „ O milionerze , który ukradł słońce ” .
W latach pięćdziesiątych dostał zadanie stworzenia edukacyjnego filmu o tym jak powstaje płótno .
Ponieważ nie spodobał mu się otrzymany scenariusz ( który zawierał same opisy maszyn , technologii itd .
) , zdecydował , że scenariusz napisze sam , a przez cały film dzieci prowadzić będzie jakieś sympatyczne zwierzątko , któremu dzieci będą wierzyć .
Wówczas powstał Krecik .
Prace nad filmem animowanym „ Jak Krecik dostał nowe spodenki ” trwały dwa lata i powiodły się .
W 1956 na festiwalu w Wenecji dostał nagrodę Weneckiego Lwa , a następnie nagrodę na festiwalu w Montevideo .
Od 1982 wybrane scenariusze tworzyła wspólnie z nim Hana Doskočilová .
W 2006 został odznaczony Medalem Za zasługi I stopnia .
Twórczość .
Miler stworzył prawie 70 filmów , z czego ok. 50 o Kreciku .
