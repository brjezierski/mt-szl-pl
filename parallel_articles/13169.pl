Zdeněk Smetana
Zdeněk Smetana ( ur .
26 lipca 1925 w Pradze , zm .
25 lutego 2016 tamże ) – czeski reżyser , animator , scenarzysta i producent .
Życiorys .
Pracował w studiu Bratři v triku a następnie jako reżyser w wytwórni Krátky Film Praha .
Stworzył popularne seriale animowane dla dzieci , prezentowane m.in .
jako wieczorynki .
Należały do nich m.in .
`` Bajki z mchu i paproci '' ( znane także jako „ Żwirek i Muchomorek ” ) , `` Wodnik Szuwarek '' , `` Psi żywot '' , które emitowano w Czechosłowacji , a później w Czechach .
Otrzymał ponad 50 nagród na czeskich i zagranicznych przeglądach i festiwalach filmowych ( m.in .
nagrodę BAFTA i berlińskiego Złotego Niedźwiedzia ) .
Zilustrował również kilka książek dla dzieci .
