Zdołbunów
Zdołbunów ( ukr .
Здолбунів , `` Zdołbuniw '' , ros .
Здолбунов , `` Zdołbunow '' ) – miasto na zachodniej Ukrainie , w obwodzie rówieńskim , siedziba rejonu zdołbunowskiego .
Od 1 stycznia 1925 do II wojny światowej siedziba powiatu zdołbunowskiego .
Historia .
Pierwsza wzmianka historyczna pojawia się w 1497 roku .
W 1569 roku włączone do Korony .
Leżało w województwie wołyńskim prowincji małopolskiej .
