Zdwórz
Zdwórz – wieś w Polsce położona w województwie mazowieckim , w powiecie płockim , w gminie Łąck .
Wieś królewska położona była w drugiej połowie XVI wieku w powiecie gąbińskim ziemi gostynińskiej województwa rawskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa płockiego .
Przez wieś przebiega droga wojewódzka nr 577 .
