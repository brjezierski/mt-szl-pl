Zdziary
Zdziary ( do roku 2013 Ździary ) – wieś w Polsce położona w województwie podkarpackim , w powiecie niżańskim , w gminie Jarocin .
Leży przy drodze krajowej nr 19 oraz drodze ekspresowej nr 19 .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnobrzeskiego .
W wyniku walk z czasów I wojny światowej we wsi pochowano poległych żołnierzy .
10 lipca 1943 Wehrmacht i SS spacyfikowały wieś .
Niemcy zamordowali Stefana Maślocha , a około 30 mieszkańców wywieźli do obozów koncentracyjnych .
W 1954 w Zdziarach powstał klub piłkarski Ludowy Zespół Sportowy Zdziary .
W sezonie 2021/2022 doszedł do finału podkarpackiego Pucharu Polski na szczeblu stalowowolskim , eliminując kolejno : Orła Rudnik nad Sanem , KS Jarocin , Olimpię Pysznica i Stal Nowa Dęba ; w finale LZS przegrał z występującą w trzeciej lidze Stalą Stalowa Wola .
