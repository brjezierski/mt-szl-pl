Kalwaria Zebrzydowska
Kalwaria Zebrzydowska – miasto w południowej Polsce , w województwie małopolskim , w powiecie wadowickim , siedziba gminy Kalwaria Zebrzydowska , założone w pierwszej poł .
XVII w. przez wojewodę krakowskiego Mikołaja Zebrzydowskiego .
Jest jednym z ośrodków miejskich aglomeracji krakowskiej .
Według danych GUS z 30 czerwca 2012 r. gmina liczyła 4454 mieszkańców .
Kalwaria Zebrzydowska jest ośrodkiem ruchu pielgrzymkowego do sanktuarium pasyjno-maryjnego oo .
bernardynów , które w 1999 r. ( manierystyczny zespół architektoniczny i krajobrazowy oraz park pielgrzymkowy z XVII wieku ) zostało , jako jedyna kalwaria na świecie , wpisane na listę światowego dziedzictwa UNESCO .
Położenie .
Miasto położone na Pogórzu Wielickim , n.p.m. , u stóp góry Żar , nad rzeką Cedron .
