Arjen Robben
Arjen Robben ( ur .
23 stycznia 1984 w Bedum ) – holenderski piłkarz , który w trakcie kariery występował na pozycji pomocnika .
W latach 2003–2017 reprezentant Holandii .
Srebrny medalista Mistrzostw Świata 2010 i brązowy medalista Mistrzostw Świata 2014 .
Kariera klubowa .
Początkowo występował w FC Groningen .
3 grudnia 2000 roku zadebiutował w Eredivisie w zremisowanym 1:1 meczu z RKC Waalwijk .
Pierwszego gola strzelił 11 marca 2001 w spotkaniu z Ajaksem Amsterdam .
W sezonach 2000/2001 i 2001/2002 regularnie występował w pierwszym zespole Groningen .
W 2002 roku przeszedł do PSV Eindhoven , w którym szybko wywalczył miejsce w podstawowym składzie .
W sezonie 2002/2003 w rozgrywkach holenderskiej ekstraklasy zdobył 12 bramek , przyczyniając się do zdobycia mistrzostwa kraju .
