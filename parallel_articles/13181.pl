Zelandia ( wyspa )
Zelandia ( duń .
`` Sjælland '' ) – największa z 443 wysp Danii i 12. wyspa Europy .
Zelandia położona jest między Fionią a szwedzką Skanią ; z obiema połączona jest mostami poprzez cieśniny Wielki Bełt i Sund .
Linia brzegowa silnie rozczłonkowana , liczne półwyspy , m.in .
Ods Herred , Horns na północy , zatoki Sejerø , Roskilde Fjord , Ise Fjord na północy ; Køge , Fakse na wschodzie oraz Vordingborska na zachodzie , mniejsze wyspy , takie jak Saltholm , Amager , Sejerø .
Na Zelandii położona jest stolica Danii – Kopenhaga , miejsce akcji szekspirowskiego Hamleta – Elsynor , oraz Roskilde , miasto znane z organizowanych corocznie dużych festiwali muzyki rockowej .
`` Uwaga '' : nazwa państwa Nowa Zelandia nie pochodzi od nazwy tej wyspy , a od regionu w Holandii .
Natomiast nazwa samej wyspy odnosi się do dużej populacji fok ( `` sæl '' ) u jej wybrzeży .
