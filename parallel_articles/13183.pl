Zelwa ( stacja kolejowa na Białorusi )
Zelwa ( ; ) – stacja kolejowa w miejscowości Zelwa , w rejonie zelwieńskim , w obwodzie grodzieńskim , na Białorusi .
Położona jest na linii Baranowicze - Wołkowysk .
Stacja powstała w czasach carskich na linii Białystok - Baranowicze , pomiędzy stacjami Wołkowysk a Jeziornica .
Jeszcze w okresie międzywojennym nie była położona w samym miasteczku tylko w jego okolicach .
Do czasów współczesnych Zelwa rozrosła się w kierunku stacji .
