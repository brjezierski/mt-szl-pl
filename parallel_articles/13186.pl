Zendaya
Zendaya Maree Stoermer Coleman ( ur .
1 września 1996 w Oakland ) – amerykańska aktorka i piosenkarka , karierę rozpoczęła jako dziecięca aktorka , tancerka i modelka .
Popularność zaczęła zdobywać dzięki występom w produkcjach Disneya , m.in .
w serialach `` Taniec rządzi '' oraz `` K.C .
Nastoletnia agentka '' .
Wystąpiła także w filmie `` Król rozrywki '' i trylogii filmów o `` Spider-Manie '' należącej do Filmowego Uniwersum Marvela .
Za rolę pierwszoplanową w serialu dramatycznym `` Euforia '' otrzymała nagrodę Primetime Emmy , stając się zarazem najmłodszą zwyciężczynią tej nagrody w historii plebiscytu .
W 2013 wydała debiutancki album studyjny , zatytułowany po prostu `` Zendaya '' , który znalazł się na 51. miejscu listy sprzedaży `` Billboard 200 '' , a pochodzący z płyty singiel „ Replay ” dotarł do 40. miejsca na liście Bilboard `` Hot 100 '' .
W 2018 nagrany przez nią singiel „ Rewrite the Stars ” ( ze ścieżki dźwiękowej do filmu `` Król rozrywki '' ) dotarł do pierwszej dwudziestki na listach przebojów w Wielkiej Brytanii i Australii oraz był certyfikowany jako podwójnie platynowa bądź złota płyta w USA , Kanadzie , Wielkiej Brytanii i Australii .
W 2015 została uhonorowana tytułem Barbie Shero .
