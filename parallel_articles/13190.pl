Zeus ( wirus komputerowy )
Zeus , ZeuS , Zbot – koń trojański modyfikujący wygląd stron internetowych w celu wyłudzenia pieniędzy .
Infekcja Zeusem następuje wskutek pobrania i uruchomienia zainfekowanego pliku przez użytkownika lub odwiedzenia zainfekowanej strony internetowej .
Po infekcji trojan modyfikuje wygląd i działanie oryginalnej strony internetowej po stronie klienta .
Podczas wizyty na stronie internetowej banku wirus ładuje fałszywą , wcześniej spreparowaną , stronę .
Użytkownik otrzymuje rzekomo pochodzącą z banku informację o tym , że bank w ramach testowania bankowości internetowej prosi o wpłacenie podanej kwoty na określony rachunek .
Komunikat ten skłania użytkownika , dzięki socjotechnice , do dokonania wpłaty na konto „ słupa ” .
Wymieniona kwota jest najwyższą możliwą do wyprowadzenia z danego rachunku bankowego .
Operacja ta jest autoryzowana przy użyciu metody autoryzacji banku , z której korzysta użytkownik ( na przykład kodem SMS ) .
