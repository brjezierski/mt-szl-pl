Zeuthen ( stacja kolejowa )
Zeuthen ( ) – stacja kolejowa w Zeuthen , w kraju związkowym Brandenburgia , w Niemczech , na południe od Berlina .
Znajduje się na linii Berlin – Görlitz i obsługuje pociągi S46 S-Bahn w Berlinie .
Według DB Station & amp ; Service ma kategorię 5 .
Położenie .
Stacja znajduje się w gminie Zeuthen w powiecie Dahme-Spreewald .
Centrum Zeuthen znajduje się około 500 metrów od stacji , a centrum Berlina około 25 kilometrów w linii prostej na północny zachód .
Graniczy z Bahnstraße i Friesenstraße .
Stacja Eichwalde znajduje się około 2,8 km dalej na północ , a stacja Wildau około 3,4 km na południe .
Stacja znajduje się w strefie taryfowej Berlin C Verkehrsverbund Berlin-Brandenburg
