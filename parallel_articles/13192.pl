Zgierz
Zgierz – miasto w Polsce w województwie łódzkim , siedziba powiatu zgierskiego .
Leży nad Bzurą .
Graniczy od południa z Łodzią i wchodzi w skład aglomeracji łódzkiej .
W latach 1975–1998 miasto administracyjnie należało do ówczesnego województwa łódzkiego .
Zgierz uzyskał prawa miejskie przed rokiem 1288 i jest jednym z najstarszych miast w regionie .
Był miastem królewskim Korony Królestwa Polskiego w starostwie łęczyckim w powiecie łęczyckim województwa łęczyckiego w końcu XVI wieku .
Podczas II wojny światowej na obszarze ziem polskich włączonych do III Rzeszy w „ Warthegau ” ( „ Kraj Warty ” ) pod nazwą Görnau .
Najczęściej używana jest gwara łódzka z silniej zaznaczonymi rusycyzmami i germanizmami .
