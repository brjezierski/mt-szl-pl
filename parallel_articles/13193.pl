Zgorzelec
Zgorzelec ( , grnłuż .
`` Zhorjelc '' , dlnłuż .
`` Zgórjelc '' , ) – miasto przygraniczne w województwie dolnośląskim , w polskiej części Górnych Łużyc , na prawym brzegu Nysy Łużyckiej , w Obniżeniu Żytawsko-Zgorzeleckim .
Przed 1945 stanowiło prawobrzeżną część miasta Görlitz .
Zgorzelec jest siedzibą gminy wiejskiej Zgorzelec oraz powiatu zgorzeleckiego .
Miasto należy do Związku Miast Polskich .
Według danych GUS z 30 czerwca 2020 r. miasto miało 30 047 mieszkańców .
Barwy herbu i flagi Zgorzelca ( niebiesko-żółte ) są identyczne jak we fladze Górnych Łużyc , na terenie których Zgorzelec leży .
