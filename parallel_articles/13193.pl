W latach 1975–1998 miasto administracyjnie należało do województwa jeleniogórskiego .
Toponimia .
Nazwa pochodzi od wyrazu `` gorzeć '' ( psł .
* '' gorěti '' ) .
Określała ona miejsce wypalone , osadę powstałą na miejscu wypalonym .
Dzisiejsza polska nazwa wzięła się od form łużyckich , tj .
`` Zgórjelc '' i `` Zhorjelc '' .
Formą zniemczoną jest `` Görlitz '' .
