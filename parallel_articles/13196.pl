Agrafka
Agrafka ( : „ spinka , klamra , zapinka , przypinacz ” ) – rodzaj zapinki wykonanej z drutu , której jeden koniec zahacza się za zagięcie znajdujące się na drugim końcu .
Służy do szybkiego łączenia dwóch elementów tkaniny lub , w przypadku agrafki wędkarskiej , sztucznej przynęty z żyłką .
Historia agrafek .
Agrafki stosowano już za czasów cesarza bizantyjskiego Bazylego II .
W przeszłości zwykle miały charakter ozdobny .
Archeolodzy nazywają klasyczne starożytne agrafki fibulami .
W Polsce dawniej zwane były zapinkami .
Stanowiły element polskiego stroju służący do przypinania piór do czapki .
