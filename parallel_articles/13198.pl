Zieleniec ( Toruń )
Osiedle Zieleniec – jedno z prawobrzeżnych osiedli Torunia .
Jest częścią dzielnicy Bydgoskie Przedmieście .
Pochodzenie nazwy osiedla .
Nazwa pochodzi od dużej ilości terenów zielonych , okolicznych parków oraz dużego Parku Miejskiego .
Historia .
Najwcześniejsze ślady bytności człowieka na tym obszarze sięgają neolitu .
W średniowieczu część dzisiejszego osiedla położoną najbliżej Wisły zamieszkiwali rybacy i flisacy , na terenie przedmieścia była także wydobywana glina służąca za surowiec toruńskim cegielniom .
W tamtym okresie osiedle miało charakter podmiejskich rezydencji oraz rozległych ogrodów bogatszej części mieszkańców .
W XIX wieku osiedle stało się placem intensywnej zabudowy dla przeżywającego duży rozkwit Torunia , któremu brakowało terenów pod inwestycje w ciasnej zabudowie średniowiecznej Starówki .
W roku 1817 powstał zamysł stworzenia rozległego Parku Miejskiego , który z racji tego , iż swym zasięgiem obejmował tereny użytkowane wcześniej przez cegielnię , nazwany został `` Parkiem Cegielnia '' ( `` Ziegelei-Park '' ) .
Jest to jeden z najstarszych parków w Polsce o charakterze otwartym ( udostępnionym publicznie ) .
W roku 1884 na zachodnim skraju osiedla powstały koszary ułanów .
Na przełom XIX i XX w. przypada gwałtowny rozwój budownictwa mieszkaniowego .
16 maja 1891 r. osiedle zostało połączone linią tramwajową z centrum Torunia .
