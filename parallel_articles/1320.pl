Arkada
Arkada ( łac .
`` arcus '' - łuk ) – element architektoniczny składający się z dwóch podpór ( kolumn , słupów lub filarów ) , które zostały połączone u góry łukiem .
Arkada występuje w architekturze zarówno pojedynczo , jak i najczęściej w rzędzie , tworząc istotny składnik budowli .
Znana jest od czasów starożytnego Rzymu , stosowana głównie w akweduktach , krużgankach , loggiach .
Arkada ślepa , inaczej blenda arkadowa , to arkada z otworem zamkniętym ścianą , do której arkada przylega .
Ciągi ślepych arkad stanowią czasem główny element fryzów ( tzw .
fryz arkadkowy ) lub attyk .
