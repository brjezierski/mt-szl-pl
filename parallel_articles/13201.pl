Ziernograd ( ros .
Зерноград ) — miasto w Rosji , w obwodzie rostowskim , siedziba administracyjna rejonu ziernogradskiego .
Miasto znajduje się na Stepie Salskim , 77 km od Rostowa nad Donem .
W mieście działa stacja kolejowa Kolei Północnokaukaskiej .
Miejscowość została założona w 1933 roku .
W 1951 roku otrzymała prawa miejskie .
