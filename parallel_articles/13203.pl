Zinédine Zidane
Zinédine Yazid Zidane ( ur .
23 czerwca 1972 w Marsylii ) – francuski piłkarz występujący na pozycji pomocnika , po zakończeniu kariery zawodniczej trener piłkarski .
Podczas swojej kariery piłkarza nosił przydomek „ Zizou ” .
108-krotny reprezentant swojego kraju , z którym zdobył Mistrzostwo Świata 1998 oraz Mistrzostwo Europy 2000 .
Laureat Złotej Piłki w 1998 roku , najlepszy zawodnik Mistrzostw Świata 2006 , na których jako kapitan drużyny narodowej dotarł z nią do finału .
Zawodnik AS Cannes , Girondins Bordeaux , Juventusu oraz Realu Madryt , z którym w 2002 roku zwyciężył w rozgrywkach Ligi Mistrzów .
Trzy razy wybierany Piłkarzem Roku FIFA , raz wybierany najlepszym zawodnikiem w Europie .
W 2004 roku Brazylijczyk Pelé umieścił go na liście stu najlepszych żyjących piłkarzy na świecie .
Piłkarską karierę Zidane zakończył w 2006 roku po mistrzostwach świata .
Kariera klubowa .
Początki kariery i pobyt w AS Cannes .
Zinédine Zidane już w 1982 roku dołączył do szkółki juniorów klubu US Saint-Henri , którego stadion znajdował się w jednej z dzielnic Marsylii – La Castellane .
Następnie młody Francuz trenował w juniorskich zespołach SO Septèmes-les-Vallons .
W wieku czternastu lat wyjechał z Septèmes-les-Vallons na nabór do ligi juniorów .
Wówczas przykuł na siebie uwagę skauta klubu AS Cannes – Jeana Varrauda .
Debiut w dorosłej kadrze tej drużyny Zidane zaliczył jako szesnastolatek dnia 20 maja 1989 , a swoją pierwszą bramkę dla AS Cannes strzelił 8 lutego 1991 roku .
W nagrodę za tego gola francuski gracz otrzymał od prezydenta klubu samochód .
W debiutanckim sezonie w zespole z Cannes Zidane rozegrał dwa ligowe spotkania , jednak w kolejnych rozgrywkach na boisku nie pojawił się już ani razu .
Sezon 1990/1991 rozpoczął już jednak jako podstawowy zawodnik podopiecznych Boro Primoraca i wystąpił w 28 meczach Ligue 1 .
W kolejnym sezonie zaliczył 31 występów i strzelił pięć bramek , dzięki czemu został jednym z najlepszych strzelców drużyny .
Bordeaux .
Latem 1992 roku Zidane został piłkarzem Girondins Bordeaux .
Już od początku pobytu w ekipie „ żyrondystów ” francuski pomocnik był podstawowym graczem zespołu .
Pierwszy sezon w nowym klubie zakończył z 35 występami i 10 bramkami w ligowych rozgrywkach .
Razem z Bordeaux Zidane w 1995 roku zwyciężył w Pucharze Intertoto , a w 1996 roku dotarł do finału Pucharu UEFA .
Przez cztery lata gry w tym klubie Francuz w linii pomocy miał okazję grać u boku takich piłkarzy jak Bixente Lizarazu , Christophe Dugarry , Richard Witschge oraz Philippe Vercruysse .
W 1995 roku chęć pozyskania Zidane ’ a oraz Dugarryego wyraził trener Blackburn Rovers – Kenny Dalglish .
Właściciel zespołu – Jack Walker skomentował to jednak mówiąc `` „ po co kupować Zidane ’ a , skoro mamy w kadrze Tima Sherwooda ? ” ''
Juventus .
W maju 1996 roku Zidane podpisał czteroletni kontrakt ze zwycięzcą Ligi Mistrzów – Juventusem , do którego trafił za trzy miliony funtów .
W drużynie prowadzonej przez Marcello Lippiego grał na pozycji rozgrywającego .
Przyczynił się do wywalczenia przez „ Starą Damę ” mistrzostwa Włoch oraz Pucharu Interkontynentalnego , w którym Juventus pokonał 1:0 CA River Plate .
Podczas sezonu 1996/1997 włoski klub dotarł także do finału Ligi Mistrzów , jednak przegrał wówczas 3:1 z Borussią Dortmund .
Pierwszy rok pobytu w Turynie Zidane zakończył z 29 meczami i pięcioma bramkami w Serie A .
Strzelając siedem goli w 32 spotkaniach ligi włoskiej , Zidane pomógł Juventusowi obronić tytuł mistrza kraju .
