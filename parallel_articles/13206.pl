W 1892 Brytyjczycy , zaniepokojeni wzrostem zainteresowania innych mocarstw Zatoką Perską , zawarli z każdym z emiratów ( Abu Zabi , Adżman , Dubaj , Fudżajra , Ras al-Chajma , Szardża i Umm al-Kajwajn ) układy protekcyjne , według których polityka zagraniczna tych państw ( zwanych następnie Omanem Traktatowym ) leżała w gestii Korony brytyjskiej .
