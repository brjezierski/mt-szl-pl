XX wieku Oman Traktatowy stał się zamożny dzięki eksploatacji złóż ropy naftowej .
W 1968 roku rząd brytyjski ogłosił zamiar wycofania swoich wojsk z regionu w ciągu najbliższych 3 lat .
Już wcześniej pod brytyjskim patronatem zaczęła się tworzyć federacja emiratów ( szejkanatów ) arabskich , położonych na południowym brzegu Zatoki Perskiej .
Miała ona objąć 7 emiratów Omanu Traktatowego oraz sąsiednie księstwa Katar i Bahrajn .
Uchwała o utworzeniu federacji zapadła na zjeździe zainteresowanych szejków w lutym 1968 .
Jednak ambicje poszczególnych szejków oraz nierozwiązanie wielu kwestii formalno-prawnych nawet w tak istotnych dziedzinach , jak ustrój nowego organizmu państwowego spowodowały , że federacja pozostała głównie „ na papierze ” .
W 1971 Bahrajn i Katar zrezygnowały z uczestnictwa w federacji i utworzyły niezależne państwa .
W toku obrad , które w lipcu 1971 toczyły się w pałacu szejka Dubaju , salę opuścił także szejk Ras al-Chajmy .
18 lipca pozostałych sześciu władców podpisało umowę o utworzeniu federacji Zjednoczonych Emiratów Arabskich , która 2 grudnia 1971 oficjalnie ogłosiła niepodległość .
Nowe państwo zostało 6 grudnia przyjęte do Ligi Arabskiej , a 8 grudnia na podstawie Rezolucji Rady Bezpieczeństwa ONZ nr 304 – jako 132 członek – do ONZ .
Jeszcze 23 grudnia tego roku w Abu Zabi ogłoszono , że do federacji w przyszłym roku przystąpi również emirat Ras al-Chajma .
Zjednoczone Emiraty Arabskie są jedynym skutkiem idei panarabizmu , który przetrwał próbę czasu .
Pierwszym prezydentem Zjednoczonych Emiratów Arabskich został wybrany na 5 lat Zaid ibn Sultan an-Nahajan , władca Abu Zabi , a wiceprezydentem szejk Dubaju .
Władcy poszczególnych emiratów utworzyli Radę Najwyższą , do kompetencji której należały sprawy polityki zagranicznej , obrony , kluczowe decyzje gospodarcze .
Każdy emirat zachował przy tym własną administrację i samodzielne kierowanie sprawami wewnętrznymi , często jeszcze uwarunkowane lokalnymi tradycjami plemiennymi .
Ustalono , że budżet federacji będzie tworzony ze składek wynoszących po 10 % dochodów każdego z emiratów .
Organem ustawodawczym zostało Zgromadzenie Doradcze , złożone z 34 delegatów wybieranych przez poszczególne emiraty w ilości proporcjonalnej do liczby ludności .
Tymczasową stolicą państwa została rezydencja szejka Abu Zabi – el-Ain w oazie Burajmi .
W 1979 zerwano stosunki dyplomatyczne z Egiptem za podpisanie układu wojskowego z Izraelem .
W 1985 zostały nawiązane stosunki dyplomatyczne i handlowe z ZSRR i Chinami , a w 1987 wznowiono stosunki dyplomatyczne z Egiptem .
W 1990 ZEA poparły interwencję w Iraku i zgodziły się na rozlokowanie na swoim terytorium wojsk francuskich i brytyjskich .
W 1994 ZEA podpisały układ obronny ze Stanami Zjednoczonymi .
W 2004 po śmierci prezydenta jego miejsce zajął Chalifa ibn Zaid an-Nahajan .
W 2006 po śmierci premiera Maktuma ibn Raszida al-Maktuma jego urząd przejął Muhammad ibn Raszid al-Maktum .
Geografia .
Zjednoczone Emiraty Arabskie położone są w strefie klimatu zwrotnikowego kontynentalnego suchego , który w głębi kraju staje się skrajnie suchy .
Powierzchnia kraju jest w większości nizinna ( 100-150 metrów ) i pustynna ( około 97 % powierzchni państwa zajmuje pustynia Ar-Rub al-Chali ) .
Na wschodzie kraju wznoszą się niewielkie góry Al-Hadżar al-Gharbi .
Najwyższym szczytem państwa jest góra Dżabal Jibir ( 1517 m n.p.m .
) , jednakże nie jest to najwyższy punkt kraju , ten bowiem leży na zboczu góry Dżabal al-Dżajs ( 1910 m n.p.m .
) , której szczyt wznosi się już na terytorium Omanu .
Średnie temperatury w styczniu wynoszą około 20 °C , w lipcu 32-40 °C ( maksymalnie wynoszą ponad 50 °C ) .
Opady deszczu występują głównie w zimie ( grudzień-luty ) , a ich roczna suma to 80-140 mm .
Latem na wybrzeżu utrzymuje się wysoka wilgotność .
W emiratach nie znajdują się żadne rzeki stałe , nielicznie występują wadi , zaś na wody artezyjskie można trafić w oazach Al-Ajn i Al-Burajmi .
Wśród licznych solnisk największym jest Sabchat Matti .
Roślinność jest skąpa , głównie są to halofity .
Na niskim , częściowo zabagnionym wybrzeżu Zatoki Perskiej , zlokalizowane są rafy koralowe i przybrzeżne wysepki , wybrzeże Zatoki Omańskiej jest z kolei górzyste .
Linia brzegowa kraju jest dobrze rozwinięta ( jej długość wynosi około 700 kilometrów ) .
