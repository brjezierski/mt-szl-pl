Arkansas
Arkansas ( ) – stan w południowo-wschodniej części Stanów Zjednoczonych , sąsiadujący z Missouri , Oklahomą , Teksasem , Luizjaną , Missisipi i Tennessee .
Większość obszaru stanu jest nizinna ( dolina Missisipi ) , a południowo-zachodnia część wyżynna ( wyżyna Ozark ) .
Ponad połowę powierzchni stanu pokrywają lasy .
Arkansas znane jako Stan Naturalności jest bogate w niezliczone zasoby naturalne , w tym brom , gaz ziemny , ropę naftową i kamień krzemowy .
Najwcześniejszymi mieszkańcami tych obszarów byli Indianie żyjący wzdłuż rzeki Missisipi .
W okresie , gdy tereny te stały się obiektem zainteresowania Europejczyków , żyły tam plemiona : Caddo ( Kaddo ) , Osedżowie i Quapaw .
Konserwatywna tradycja tego stanu wyraża się w stosowaniu kary śmierci przez wstrzyknięcie trucizny , zakazem małżeństw jednopłciowych w następstwie referendum , a także głosowaniem z 2021 roku za zakazem aborcji , nawet w przypadku gwałtu lub kazirodztwa .
