Demografia .
Spis ludności z roku 2020 stwierdza , że stan Arkansas liczy 3 011 524 mieszkańców , co oznacza wzrost o 95 606 ( 3,3 % ) w porównaniu z poprzednim spisem z roku 2010 .
Dzieci poniżej piątego roku życia stanowią 6,2 % populacji , 23,2 % mieszkańców nie ukończyło jeszcze osiemnastego roku życia , a 17,4 % to osoby mające 65 i więcej lat .
50,9 % ludności stanu stanowią kobiety .
Rasy i pochodzenie .
Według danych z 2019 roku , 76,7 % mieszkańców stanowiła ludność biała ( 72 % nie licząc Latynosów ) , 15,5 % to czarni lub Afroamerykanie , 2,8 % miało rasę mieszaną , 1,5 % to Azjaci , 0,6 % to rdzenna ludność Ameryki , 0,4 % to Hawajczycy i mieszkańcy innych wysp Pacyfiku .
Latynosi stanowią 7,7 % ludności stanu .
