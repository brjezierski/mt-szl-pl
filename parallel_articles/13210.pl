Zmijiw
Zmijiw ( ) – miasto na Ukrainie w obwodzie charkowskim w rejonie czuhujewskim .
Stacja kolejowa .
Historia .
Miasto od 1797 , ujezd zmijewski guberni charkowskiej .
Od 1976 do 1990 roku miasto nosiło nazwę Gotwald .
