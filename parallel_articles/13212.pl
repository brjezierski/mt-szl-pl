Zodiomyces vorticellarius
Zodiomyces vorticellarius Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Zodiomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał Roland Thaxter w 1891 r. na owadzie `` Hydrocolombus lacustris '' w USA .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w 1999 i 2003 r. opisał jego występowanie na chrząszczach `` Helochares lividus '' i `` Helochares obscurus '' z rodziny kałużnicowatych ( Hydrophilidae ) .
