Wypominki
Wypominki ( na Górnym Śląsku `` zalecki '' ) – forma modlitwy błagalnej za zmarłych , polegająca na wyczytywaniu ich imion .
Wyróżnia się wypominki jednorazowe , oktawalne i roczne .
Bywają też półroczne i kwartalne .
Wypominki jednorazowe odczytuje się na cmentarzu , oktawalne przez osiem dni od dnia Wszystkich Świętych ( często połączone z nabożeństwem różańcowym ) , a roczne – przez cały rok przed niedzielnymi mszami .
