Zorian Dołęga-Chodakowski
Zorian Dołęga-Chodakowski , właściwie Adam Czarnocki ( ur .
4 kwietnia 1784 roku w Podhajnej koło Nieświeża , zm .
17 listopada 1825 roku ) – polski etnograf , archeolog , historyk i słowianofil ; jeden z głównych prekursorów badań nad Słowiańszczyzną , obok Adama Naruszewicza , Jana Potockiego , Józefa Maksymiliana Ossolińskiego i Wawrzyńca Surowieckiego .
W jego pracach występowały akcenty antyfeudalne i antyklerykalne .
Główna praca drukowana : `` O Sławiańszczyźnie przed chrześcijaństwem '' .
Zorian Dołęga-Chodakowski jest uważany za prekursora odnowy religii Słowian .
Życiorys .
Adam Czarnocki urodził się w niezamożnej rodzinie szlacheckiej .
Jego ojcem był oficjalista dworski Jakub Czarnocki , a matką Sekundyna z domu Borodziczówna .
Po śmierci matki ojciec oddał go w 1795 roku pod opiekę krewnego , Ksawerego Czarnockiego , podstolego witebskiego z Lecieszyna .
W majątku podstolego nie był jednak traktowany na równi z innymi członkami rodziny , co spowodowało jego rozgoryczenie i zwrócenie się ku klasie chłopskiej .
Ukończył szkołę powiatową w Słucku w 1801 roku .
Potem zaczął się przygotowywać do zawodu prawnika .
Praktykę adwokacką odbywał w Nowogródku i Mińsku .
W 1807 roku przyjął posadę plenipotenta dóbr hrabiego Józefa Niesiołowskiego i osiadł w Worończy .
W roku następnym carska policja przechwyciła jego list odpowiadający na wezwanie do wstępowania w szeregi polskiego wojska w Księstwie Warszawskim .
Nocą z 25 na 26 marca 1809 roku został aresztowany , a następnie skazany na dożywotnią służbę „ w sołdatach ” .
Wysłany do dywizji w Omsku , w czasie pieszej wędrówki na Syberię prowadził raptularz zatytułowany `` Bez chęci podróż moja '' , który obecnie pozostaje zaginiony .
Latem 1810 roku udało mu się pod Bobrujskiem uciec z dywizji .
Wstąpił do piątego pułku piechoty i z armią Napoleona zawędrował aż pod Smoleńsk .
Pseudonim Zorian Dołęga Chodakowski przyjął w 1813 roku i wtedy rozpoczął badania ludoznawcze .
Przez kilka lat wędrował po kraju , notował pieśni i podania ludowe , gromadził materiały z zakresu „ starożytności słowiańskich ” .
W 1817 roku uzyskał protekcję i wsparcie finansowe księcia Adama Jerzego Czartoryskiego .
Aby uzyskać stałą dotację instytucji naukowej na dalsze badania , napisał wiosną 1818 roku w Sieniawie rozprawę `` O Sławiańszczyźnie przed chrześcijaństwem '' .
Została opublikowana w 5. numerze „ Ćwiczeń Naukowych ” .
Jednak przedsięwzięcie Zoriana zostało ocenione negatywnie i odrzucone , najpierw na Uniwersytecie Wileńskim , a potem wśród rosyjskich mecenasów .
W 1819 roku został wybrany na członka Warszawskiego Towarzystwa Przyjaciół Nauk .
Został przychylnie przyjęty przez rosyjskie środowisko naukowe , m.in .
dzięki krytyce tekstów historyka Mikołaja Karamzina , który miał wielu wrogów w swoim środowisku .
Przedstawiony przez Chodakowskiego „ Projekt naukowej podróży po Rosji ” uzyskał subwencję , choć rok później ją stracił poprzez wpływy Karamzina .
Ożenił się z Konstancją Fleming w 1819 roku .
W ciągu dwóch lat zmarł syn z tego związku , a potem również żona .
Wkrótce Chodakowski ożenił się powtórnie .
Popadł w biedę i do końca życia nie uzyskał już finansów na badania .
Mimo to dalej ciężko pracował i gromadził materiały .
Ostatnią rozprawę , `` Próba wyjaśnienia słowa kniaź – ksiądz '' , ukończył w 1824 roku .
Pod koniec życia podjął posadę zarządcy dóbr u pewnego ziemianina w guberni twerskiej .
Tam zmarł w 1825 r. w nieznanych okolicznościach .
Dorobek naukowy .
Najważniejsze tezy zawarł w rozprawie `` O Sławiańszczyźnie przed chrześcijaństwem '' .
Chrześcijaństwo , według Chodakowskiego , zatarło cechy Słowian , zniszczyło ich dorobek kulturowy .
Elementy przedchrześcijańskiej historii , religii , kultury Słowian miały przetrwać jedynie w folklorze ludowym .
Chodakowski postulował odrodzenie narodu dzięki szeroko zakrojonym badaniom nad ludem wiejskim i jego obyczajami :
Zebrał około 2 tys .
pieśni polskich , rusińskich , rosyjskich , czeskich .
Część z nich została opublikowana jako `` Śpiewy sławiańskie pod strzechą wiejską zebrane '' .
Doceniał lud , a swoją opinie o nim zawarł w opinii `` Złe ma serce ten , kto ludu nie kocha braterską miłością '' , co znalazło w okresie późniejszym oddzwięk w ideologii romantyków .
Prócz zapoczątkowania badań etnograficznych był również prekursorem archeologii i toponomastyki .
Badał grodziska i kurhany .
Na tej podstawie opracował teorię wschodniosłowiańskich grodzisk , które nazywał „ stare-sta ” .
W okresie swego życia był również najlepszym znawcą , niedawno wtedy odkrytego , tekstu `` Słowa o wyprawie Igora '' .
Chodakowski na określenie Słowian używał nazwy „ Sławianie ” , którą wywodził od sławy i przytaczał na poparcie swej tezy liczne argumenty językowe .
Wpływ na literaturę .
Pokolenie romantyków ruszyło w ślady Chodakowskiego szukając wśród ludu natchnienia i budulca dla swej twórczości .
Wytworzył się nowy typ literata-wędrownika , a moda ta stała się powszechna .
Wędrówki po kraju prowadzili Seweryn Goszczyński , Ryszard Berwiński , Lucjan Siemieński , Kazimierz Władysław Wójcicki i inni .
Pieśń gminna została rozsławiona najpierw przez `` Ballady i romanse '' Mickiewicza , następnie przez `` Dziady '' Mickiewicza , `` Zamek kaniowski '' Goszczyńskiego , poezję Bohdana Zaleskiego , a przede wszystkim przez `` Pieśń Wajdeloty '' z `` Konrada Wallenroda '' .
Nazwisko Chodakowskiego po raz pierwszy pojawiło się w utworach poetyckich w 1832 r. w `` Córze Sławy '' Jana Kollara i `` Jezierskim '' Puszkina .
Pierwszym takim utworem polskim był `` Mistrz '' Dominika Magnuszewskiego .
Jako Zorian występuje też w `` Królu-Duchu '' Juliusza Słowackiego .
Był też pierwowzorem poety Justyna z powieści Kraszewskiego „ Dwa światy ” .
