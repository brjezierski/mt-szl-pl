`` Nagybecskerek '' ) – miasto w północno-wschodniej Serbii , w Wojwodinie , stolica okręgu środkowobanackiego , siedziba miasta Zrenjanin .
W 2011 roku liczyło 76 511 mieszkańców .
Zrenjanin słynie z letniego pałacu Marii Antoniny , który można podziwiać na znajdującym się w centrum miasta rynku .
Nazwa miasta pochodzi od nazwiska Žarko Zrenjanina .
W mieście znajduje się cerkiew Zaśnięcia Najświętszej Marii Panny .
Zrenjanin jest również siedzibą rzymskokatolickiej diecezji , skupiającej przeważnie wiernych narodowości węgierskiej .
