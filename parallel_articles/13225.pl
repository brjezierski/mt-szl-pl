Zupa pomidorowa
Zupa pomidorowa , także pomidorówka – zupa przygotowywana na bazie rosołu lub bulionu z warzyw .
Charakterystyka .
Głównym składnikiem dania są świeże pomidory lub koncentrat pomidorowy .
Zupę podaje się z makaronem lub ryżem , lanym ciastem czy tzw .
zacierką .
Część osób do poprawy smaku stosuje zioła , np .
bazylię , natkę pietruszki oraz zabiela śmietaną .
Zupa pomidorowa składa się na tradycyjny obiad kuchni polskiej .
Przepis na to danie był znany w Polsce przynajmniej od XIX wieku .
