Historia .
1 kwietnia 1889 roku , w wyniku połączenia mniejszych wsi , w powiecie Miura powstała .
1 kwietnia 1913 roku wioska zdobyła status miasteczka ( 町 ) i została przemianowana na Zushi .
1 kwietnia 1943 roku miasteczko zostało włączone do miasta Yokosuka .
1 lipca 1950 Zushi zostało oddzieliło się z miasta jako osobne miasteczko .
15 kwietnia 1954 roku Zushi zdobyło status miasta .
