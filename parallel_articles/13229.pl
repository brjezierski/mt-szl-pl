Zuzana Čaputová
Zuzana Čaputová z domu Strapáková ( wym .
; ur .
21 czerwca 1973 w Bratysławie ) – słowacka polityk , działaczka społeczna i prawniczka , w latach 2018–2019 wiceprzewodnicząca partii Postępowa Słowacja .
Od 2019 prezydent Słowacji , będąca pierwszą kobietą w historii Słowacji pełniącą tę funkcję .
Przed objęciem urzędu prezydenta pracowała jako prawniczka , praktykowała w zawodzie adwokata .
Współtworzyła i była wiceprzewodniczącą liberalnego ugrupowania Postępowa Słowacja .
Działała także w sektorze pozarządowym , współpracując ze stowarzyszeniem obywatelskim VIA IURIS , zajmującym się wzmacnianiem praworządności i promowaniem sprawiedliwości .
W 2016 jako prawniczka obywatelskiej inicjatywy , której celem było zamknięcie składowiska odpadów w Pezinoku , otrzymała Nagrodę Goldmanów .
Problemem tego wysypiska zajmowała się kilkanaście lat .
W 2017 ogłosiła odejście z zespołu VIA IURIS , planując dalszą działalność zawodową przy kwestiach związanych z ochroną środowiska .
