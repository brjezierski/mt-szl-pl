Zuzana Čaputová urodziła się w Bratysławie , lecz znaczną część życia spędziła w Pezinoku .
Jej matka Katarína Strapáková pracowała w administracji , ojciec Štefan Strapák był dyrektorem oddziału firmy ubezpieczeniowej .
Jej starszy brat Daniel Strapák również zaangażował się w działalność Postępowej Słowacji w Pezinoku .
Ukończyła szkołę podstawową i gimnazjum w Pezinoku .
W latach 1991–1996 studiowała na Wydziale Prawa Uniwersytetu Komeńskiego w Bratysławie .
