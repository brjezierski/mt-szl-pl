Zwardoń ( stacja kolejowa )
Zwardoń – stacja kolejowa w Zwardoniu , w województwie śląskim , w Polsce .
Stacja jest położona na wysokości 686 m n.p.m .. Linia kolejowa przechodząca przez stację Zwardoń została otwarta w 1884 roku .
Do II wojny światowej linia miała charakter międzynarodowy , następnie stacja Zwardoń była stacją końcową .
Pierwszy pociąg elektryczny wjechał do Zwardonia 15 grudnia 1986 roku .
Rozkładowe kursy uruchomiono 5 stycznia 1987 roku .
1 czerwca 1992 roku w Zwardoniu otwarto przejście graniczne kolejowe polsko-czechosłowackie .
Od 1995 roku na stacji funkcjonują przekaźnikowo-komputerowe urządzenia sterowania ruchem kolejowym .
Kasa biletowa oraz poczekalnia zostały zamknięte w 2010 roku .
Połączenia w stronę Polski obsługiwane są przez Koleje Śląskie i PKP Intercity , a w stronę Słowacji przez słowackiego przewoźnika Železničná spoločnosť Slovensko .
W roku 2021 stacja obsługiwała 150–199 pasażerów na dobę .
