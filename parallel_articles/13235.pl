Zwickau
Zwickau ( pol .
hist .
`` Ćwików '' , czes .
`` Cvikov '' ) – miasto powiatowe we wschodniej części Niemiec , w kraju związkowym Saksonia , w okręgu administracyjnym Chemnitz , stolica powiatu Zwickau , na przedgórzu Rudaw .
Historia .
Tereny wokół Zwickau zostały zasiedlone przez Słowian na początku VII wieku .
W X wieku zaczęli przybywać niemieccy osadnicy , a rodzimi Słowianie zostali schrystianizowani .
Pierwsze wzmianki o mieście pochodzą z roku 1118 .
Osada otrzymała prawa miejskie w 1212 .
Zwickau był wolnym miastem od 1290 do 1323 .
W 1470 roku po odkryciu srebra w Schneebergu nastąpił intensywny rozwój górnictwa w regionie .
Miasto zostało złupione podczas wojny 30-letniej .
W roku 1810 , w domu przy rynku , urodził się kompozytor Robert Schumann ( upamiętnia go pomnik z 1901 na Hauptplatz ) .
W 1904 r. w mieście powstała fabryka samochodów Horch , a następnie w 1909 roku wytwórnia samochodów Audi .
