Arkona ( rosyjski zespół muzyczny )
Arkona ( ros .
`` Аркона '' ) − założona w 2002 roku rosyjska grupa muzyczna wykonująca folk-pagan metal .
Grupa wydała siedem płyt LP , jedną EP oraz dwa koncertowe DVD .
Historia .
Korzenie grupy sięgają początków 2002 roku , kiedy członkowie koła rodzimowierców z miasta Dołgoprudnyj : Masza `` Scream '' Archipowa i Aleksandr `` Warlock '' Koroliew postanowili zorganizować grupę muzyczną odpowiadający ich poglądom .
Początkowo grupa nazywała się `` Hiperboreja '' ( oryg .
ros .
Гиперборея ) .
W jej skład weszli : Masza `` Scream '' Archipowa ( wokal ) , Jewgienij Kniaziew ( gitara ) , Jewgienij Borzow ( bas ) , Ilja Bohatyriew ( gitara ) , Aleksandr « Warlock » Koroliew ( bębny ) , Olga Łoginowa ( klawisze ) .
Już w lutym tego samego roku grupa zmieniła nazwę na Arkona ( oryg .
ros .
Аркона ) .
Tematyka utworów początkowo była zorientowana na folk-pagan metal , z tekstami nawiązującymi do rodzimowierstwa i historii Rusi .
W związku z rosnącą popularnością grupy , zaistniała potrzeba nagrania dema .
Miało to miejsce w końcu 2002 roku w studiu CDM-records .
Demo zawierało trzy utwory : `` Kołada '' , `` Sonceworot '' , `` Ruś '' .
Materiał ten można znaleźć na bonus-tracku albumu `` Żyzń wo sławu '' .
Od początku 2003 roku grupa rozpoczęła koncertowanie .
Pierwszą większą okazją do zaznajomienia publiczności z twórczością grupy był festiwal Jazyczeskaja Ruś w klubie `` Estakada '' , gdzie 8 lutego 2003 roku grupa zaprezentowała się obok takich sław rosyjskiej sceny muzycznej , jak Butterfly Temple , Pagan Reign , Сварга ( Swarga ) , Rossomahaar .
Po serii koncertów zespół rozpoczął starania o nagranie debiutanckiego albumu , lecz zostały one przerwane zawieszeniem działalności grupy .
Wokalistka grupy liczyła się z możliwością rozpoczęcia kariery solowej .
Wkrótce jednak Masza powróciła do zespołu i wzięła udział w nagraniu albumu `` Wozrażdienie '' , który ukazał się w marcu 2004 roku .
Album ten zyskał szerokie uznanie w świecie i jest jednym z najważniejszych nagranych w stylu słowiańskiego pagan metal .
Głównym autorem tekstów była Masza , pomagał jej współpracujący z grupą muzyk i poeta Lesjar Niewid , były członek Butterfly Temple .
Grupa postanowiła wykorzystać sukces i już w grudniu tego samego roku ukazał się drugi album , `` Lepta '' .
Nagrano go w tym samym składzie osobowym , co poprzedni .
Sięgnięto po te same tematy , co w poprzednim przypadku , lecz utwory cechowała znacznie mroczniejsza atmosfera .
W momencie wydania albumu skład zespołu zmienił się : tworzyły go już tylko cztery osoby : Masza `` Scream '' Archipowa , Siergiej `` Lazar '' , Rusłan `` Kniaź '' i Wład `` Artist '' .
Pomimo kłopotów finansowych grupy , trzeci album pojawił się w 2005 roku .
Nagrywając go , grupa zadecydowała o odejściu od stosowania syntezatorów na rzecz oryginalnych instrumentów .
Wskutek tego zaszła konieczność współpracy z muzykami folkowymi .
Byli to przede wszystkim Władimir Czerepowskij oraz byli muzycy grup Swarga i Ałkonost .
Przy okazji prezentacji albumu nagrano płytę typu `` live '' .
Jesienią 2007 ukazała się czwarta płyta Arkony , `` От сердца к небу '' .
31 października 2008 grupa podczas koncertu w moskiewskim klubie `` Точка '' ( Toczka ) zarejestrowała materiał przewidziany do wydania tego samego roku w formie zapisu koncertu na płycie DVD .
Tytuł wydawnictwa : `` Ночь Велесова '' ( Nocz Welesowa ) .
28 października 2009 zespół wydał płytę `` Goi Rode Goi '' .
