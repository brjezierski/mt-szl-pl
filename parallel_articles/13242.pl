Zwiniacz
Zwiniacz ( ukr .
: Звиняч , `` Zwyniacz '' ) – wieś na Ukrainie , w rejonie czortkowskim obwodu tarnopolskiego , w hromadzie Białobożnica leżąca nieopodal rzeki Seret w czworoboku pomiędzy Czortkowem , Buczaczem , Trembowlą a Kopyczyńcami .
Do 1939 ( wybuch II wojny światowej ) w granicach II Rzeczypospolitej ( formalnie do 1945 ) .
Po II wojnie światowej obszar ten znalazł się w ZSRR .
Historia i opis .
23 sierpnia 1422 Władysław Jagiełło nadał Mykicie Swininiczowi z Mikuliniec wsie : Zwiniacz , Skomorosze i Kosów w powiecie trembowelskim .
Od około 1700 r. do aneksji przez ZSRR w 1945 r. Zwiniacz stanowił dziedzictwo rodu Mysłowskich .
