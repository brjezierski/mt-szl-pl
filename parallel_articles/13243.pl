Zegar ( jaskinia )
Zegar – jaskinia w miejscowości Strzegowa w województwie małopolskim , w powiecie olkuskim , w gminie Wolbrom .
Nazywana jest również Jaskinią Zegarową .
Znajduje się w lewych zboczach Doliny Wodącej , w Dolnych Skałach na wzniesieniu Zegarowych Skał na Wyżynie Częstochowskiej .
Historia poznania i eksploatacji .
Miejscowej ludności jaskinia znana była od dawna .
W piśmiennictwie wymieniana była już w 1846 r. , w 1908 r. Przesmycki sporządził jej pierwszy plan .
W 1997 r. zarząd Zespołu Jurajskich Parków Krajobrazowych aby ochronić jaskinię przed dewastacją zamknął jej główny otwór wejściowy metalową kratą , a wejście środkowe i boczne betonowymi korkami z niewielkim prześwitem .
Zamknięcie to miało również chronić prowadzone w niej wykopaliska archeologiczne przed zniszczeniem , a turystów przed wpadnięciem do wykopów .
Ponadto chciano w ten sposób zabezpieczyć zamontowany w jaskini sprzęt pomiarowy do obserwacji klimatycznych oraz punkty poboru wody .
Zamknięcie jednak nie spełniło swojego zadania – wkrótce uległo zniszczeniu .
Obecnie jaskinia jest dostępna , podlega jednak ochronie prawnej jako pomnik przyrody .
Autor pierwszej publikacji o jaskini z 1846 r. pisze , że na jej dnie znajdowała się tłusta i czarna ziemia , którą wydobyto w celu użyźnienia okolicznych pól .
Znajdowały się w niej kości zwierzęce i skorupy naczyń .
W ten sposób częściowo zniszczone zostało stanowisko archeologiczne .
W 1997 r. jednak archeolodzy rozpoczęli w jaskini badania .
W jej namulisku wykonano cztery wykopy ; pierwszy przed głównym wejściem do jaskini , drugi pod okapem środkowego otworu , trzeci w głównej sali jaskini , przy wlocie do niej korytarza , czwarty w najszerszym miejscu korytarza .
Pierwsze dwa wykopy zasypano po skończeniu w nich badań .
Trzeci wykop w 1998 r. osiągnął głębokość 3 m , czwarty 4 m i nie dotarł do skalistego dna jaskini .
Już w 1997 r. zebrano z wykopów 2187 szczątków zwierzęcych należących do 20 gatunków ssaków .
Oprócz zwierząt dziko żyjących były to również szczątki zwierząt domowych ( krowy , owce , kozy ) .
