Na ogół musztardy o tej samej nazwie , lecz różnych wytwórców , znacznie różnią się od siebie smakiem .
Zastosowanie .
Musztarda używana jest do poprawiania smaku potraw , głównie mięs , wędlin .
Jest stosowana także w celu nadania mięsu odpowiedniej konsystencji ( mięso staje się bardziej kruche ) .
Musztarda jest podstawowym składnikiem sosów musztardowych .
Cechami dyskwalifikującymi przydatność do spożycia są : pleśń , ślady fermentacji oraz wytrącająca się wodnisto-oleista ciecz na powierzchni .
