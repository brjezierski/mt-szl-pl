Musztarda
Musztarda – zimny sos , którego głównym składnikiem są nasiona gorczycy – białej , czarnej lub sarepskiej ; w wielu językach , w tym niemal wszystkich europejskich , nosi tę samą nazwę , co roślina , z której nasion jest zrobiona .
Powstała w starożytnym Rzymie na początku naszej ery i od początku uznawano ją za przyprawę o wartościach leczniczych ( łagodzi bóle reumatyczne , pobudza trawienie tłuszczu , działa antybakteryjnie oraz obniża ciśnienie krwi ) .
Rodzaje musztard .
Musztarda powstaje w wyniku łączenia nasion gorczycy ( zwykle mielonych ) z octem .
Dodaje się także sól i cukier .
Poprzez zastosowanie różnych gatunków gorczycy i sposobu ich mielenia ( stopnia rozdrobnienia ) , a także ze względu na różne proporcje składników i dodatków powstało wiele odmian musztardy , m.in .
