Zyndranowa
Zyndranowa ( j. łemkowski Зындранова ) – wieś w Polsce , w województwie podkarpackim , w powiecie krośnieńskim , w gminie Dukla .
Położenie .
Wieś leży w sercu Beskidu Niskiego , tuż pod głównym grzbietem Karpat , ok. 2 km na północny wschód od siodła Przełęczy Dukielskiej .
Jej zabudowania ciągną się wzdłuż doliny potoku Panna , zwanego też `` Sołotwiną '' ( niegdyś `` Sołotwyna '' ) .
Od zachodu dolinę ograniczają wzgórza Kiczera Telepkowa ( 534 m n.p.m. , na południu ) i Kiczera Gablacka ( 533 m n.p.m. , na północy ) , które oddzielają wieś od Barwinka i biegnącej przez tę wieś drogi krajowej nr 19 .
Od wschodu i północnego wschodu ogranicza dolinę ramię górskie ze szczytami ( licząc od południa ) : Kiczera ( Klepke ; 670 m n.p.m .
) , Średni Wierch ( 616 m n.p.m .
) , Tokarnia ( 692 m n.p.m .
) , Czerwony Horb ( 618 m n.p.m . )
i Wysoki Dział ( 616 m n.p.m . ) .
Na południu i południowym zachodzie tereny Zyndranowej sięgają po główny grzbiet wododziałowy Karpat w rejonie Jałowej Kiczery i biegnącą nim granicę polsko-słowacką .
