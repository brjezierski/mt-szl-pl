Zábřeh ( Ostrawa )
Zábřeh ( niem .
`` Heinrichsdorf '' ) – jedna z 37 części miasta statutarnego Ostrawy , stolicy kraju morawsko-śląskiego , we wschodnich Czechach , tworzy północną część obwodu miejskiego Ostrava-Jih , w morawskiej części miasta .
Jest to także jedna z 39 gmin katastralnych o nazwie Zábřeh nad Odrou o powierzchni 743,9859 ha , w przeszłości należały do niego również gminy katastralne Zábřeh-Hulváky i Zábřeh-VŽ ( obecnie w granicach Witkowic ) .
Populacja w 2001 wynosiła 44601 osób , zaś w 2012 odnotowano 2173 adresy .
Historia .
Pierwsza wzmianka pochodzi z 1288 .
Po wielu zmianach właścicieli w 1652 miejscowość stała się własnością ołomunieckiej kapituły i wraz z niektórymi sąsiednimi gminami w ramach tzw .
państwa petřvaldzkiego w jej rękach pozostała do upadku feudalizmu w 1848 .
W XIX wieku doszło do uprzemysłowienia okolicy .
Nieopodal uruchomiono hutę w Witkowicach , a w samym Zábřehu m.in .
cementownia , zakłady chemiczne , cegielnia .
W 1924 miejscowość została przyłączona do Morawskiej Ostrawy .
W latach 30 .
XX wieku rozwinęła się tu dzielnica willowa .
