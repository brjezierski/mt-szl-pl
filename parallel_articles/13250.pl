Zákupy ( stacja kolejowa )
Zákupy – stacja kolejowa w miejscowości Zákupy , w kraju libereckim , w Czechach .
Jest ważnym węzłem kolejowym o znaczeniu regionalnym .
Znajduje się na wysokości 270 m n.p.m .
Jest zarządzana przez Správę železnic .
Na stacji nie ma możliwości zakupu biletów , a obsługa podróżnych odbywa się w pociągu .
