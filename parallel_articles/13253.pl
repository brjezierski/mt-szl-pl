Złe Mięso
Złe Mięso ( niem .
`` Bösenfleisch '' ) – wieś pogranicza borowiacko-kaszubskiego w południowej części województwa pomorskiego , w powiecie chojnickim , w gminie Czersk nad Wdą i w kompleksie leśnym Borów Tucholskich .
Nazwa miejscowości .
Miejscowość notowana była w formie niemieckiej w 1480 jako `` Boszfleyth '' , w 1495 jako `` Bösem fleisch '' .
Polskie nazwy pojawiają się w 1585 ( `` Liemieszo '' ) i 1664 ( `` Złe Mięso '' ) .
Od XVII wieku nazewnictwo niemieckie i polskie stabilizuje się w formach `` Bösenfleisch '' i `` Złe Mięso '' .
Nazwa wsi wywodzi się od niemieckiego nazwiska właściciela miejscowej karczmy .
Forma polska jest wtórna i stanowi jego dosłowne tłumaczenie ( „ böse ” – „ zły ” , „ wściekły ” ; „ Fleisch ” – „ mięso ” ) .
