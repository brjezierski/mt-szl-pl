Złota ( dopływ Sanu koło Kuryłówki i Łazowa )
Złota – rzeka , prawobrzeżny dopływ Sanu o długości ok. 23,3 km i powierzchni zlewni ok. 100,5 km2 .
Źródła ma w województwie lubelskim , a na znacznej swej długości płynie wzdłuż granicy województwa lubelskiego i podkarpackiego .
Przepływa m.in .
przez miejscowości : Zagródki , Wólka Łamana , Kulno , Łazów i Kuryłówka .
