Złoty Potok ( dopływ Łapszanki )
Złony Potok – potok , dopływ Łapszanki .
Jego zlewnia znajduje się na południowych stokach Pienin Spiskich w obrębie wsi Łapsze Niżne w województwie małopolskim , w powiecie nowotarskim , w gminie Łapsze Niżne .
Potok wypływa u północno-wschodnich podnóży Ostrej Góry i płynie w północno-wschodnim kierunku .
U podnóży stoku przełęczy Ślipce zmienia kierunek na południowo-wschodni i płynie dnem doliny między Barwinkową i jej południowo-wschodnim grzbietem , a Ostrą Górą i Szubieniczną Górą .
Na wysokości 550 m uchodzi do Łapszanki jako jej lewy dopływ .
Ma długość około 2,5 km i kilka niewielkich dopływów .
