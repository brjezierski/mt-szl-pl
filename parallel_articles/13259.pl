Graniczy z Leśnicą ( na zachodzie ) i Stabłowicami ( od północy ) , a od południa ( wzdłuż linii kolejowej z Wrocławia przez Leśnicę do Legnicy ) z Jerzmanowem , Osińcem i Żernikami .
Na wschód i północny wschód od Złotnik znajduje się prawie niezamieszkany teren osiedla Gajowa nad strumieniem Ługowina , a dalej na wschód - Kuźniki .
W zachodniej części osiedla , pomiędzy Ryńką a Bystrzycą , znajduje się Park Złotnicki , a na jego południowo-zachodnim skraju - Zakłady Chemiczne `` Złotniki '' , jedna z najstarszych fabryk chemicznych na Dolnym Śląsku , produkująca dziś m.in .
