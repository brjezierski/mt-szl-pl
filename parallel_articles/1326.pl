Armagh
Armagh ( irl .
`` Ard Mhacha '' ) – miasto w południowej części Irlandii Północnej , stolica hrabstwa o tej samej nazwie .
Miasto zamieszkuje 14 590 mieszkańców ( 2001 ) .
Historyczne centrum religijne Irlandii – opactwo i biskupstwo założył tu w 444 roku święty Patryk .
Przez 700 lat Armagh było również siedzibą królów Ulsteru .
W 832 roku klasztor został złupiony przez wikingów .
