Następnie po 1606 wyburzono przedzamcze , budując na jego miejscu zamek dolny , wraz z reprezentacyjną bramą oraz zabudowaniami gospodarczymi .
Pomimo destruktywnej dla zamku okupacji Głogówka przez wojska szwedzkie podczas wojny trzydziestoletniej nie zaniechano prac budowlanych , co więcej , Georg III założył bibliotekę , uważaną później za jedną z największych na Górnym Śląsku .
