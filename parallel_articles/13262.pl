Zamek w Głogówku
Zamek Oppersdorffów w Głogówku – dawna rezydencja książęca i rycerska , długoletnia siedziba rodu Oppersdorffów .
Zamek składa się z dwóch części , tzw .
zamku górnego i dolnego .
Zlokalizowany jest na usytuowanej w północno-zachodnim narożu historycznej części Głogówka , wysokiej skarpie wznoszącej się przy dolinie rzeki Osobłogi .
Zbudowany został w stylu manierystycznym i barokowym , stanowi cenny przykład architektury rezydencjonalnej i obronnej na Górnym Śląsku i w Polsce .
Historia .
Dzieje zamku sięgają średniowiecza .
Miasto było wzmiankowane wcześniej – w 1076 , zaś w 1275 roku otrzymało prawa miejskie , które nadał mu książę Władysław Opolski .
Prawdopodobnie tenże władca zainicjował wzniesienie warowni .
Powstanie pierwszego zamku datuje się na przełom XIII i XIV stulecia .
Zamek należał do książąt opolskich od czasów panowania Bolka I do 1532 , kiedy po śmierci księcia Jana II Dobrego wygasła opolska linia Piastów .
Miasto i zamek przejęli na krótki okres Zedlitzowie .
Małżeństwo Christiny von Zedlitz z Hansem Oppersdorffem , które miało miejsce w 1561 otworzyło nowy rozdział w historii zamku – Oppersdorffowie władali zamkiem do 1945 roku .
Jeszcze w XVI wieku dokonano gruntownej przebudowy zamku : wpierw po 1561 roku rozebrano gotycki zamek z wieżą mieszkalną , w latach 1561–1571 Hans Oppersdorff wzniósł trójskrzydłowy gmach , wraz z kaplicą , stanowiący rdzeń zamku górnego , w 1584–1606 rozbudowano zamek górny dodając m.in .
