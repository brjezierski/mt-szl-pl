Ávila ( prowincja )
Ávila ( ) – prowincja we wspólnocie autonomicznej Kastylia i León w Hiszpanii .
Graniczy z prowincjami : Valladolid , Toledo , Cáceres , Segowia , Salamanka , a także ze wspólnotą autonomiczną Madryt .
Stolicą prowincji jest miasto Ávila .
Prowincja liczyła w 2005 roku 167 032 mieszkańców .
Powierzchnia prowincji to 8050 km² .
Kod pocztowy do wszystkich miejscowości na terenie prowincji Ávila rozpoczynają cyfry 05 .
