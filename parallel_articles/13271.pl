Zastosowanie .
Jest używana w języku portugalskim , kaszubskim , wietnamskim , arumuńskim , guarani , w etnolekcie śląskim , w języku ! Xóõ oraz w zapisie międzynarodowego alfabetu fonetycznego .
Język portugalski .
W języku portugalskim oznacza samogłoskę nosową przypisywaną w międzynarodowym alfabecie fonetycznym dla symbolu /ɐ̃/ .
W dwugłosce ãe jest czytane /ɐ̃ĩ̯/ a w dwugłosce ão /ɐ̃ũ/ .
Jest czasami wykorzystywana jako symbol języka portugalskiego .
Nie występuje w alfabecie portugalskim .
Język kaszubski .
W języku kaszubskim jest trzecią literą alfabetu .
Wymawiana jest /ã/ a w powiecie puckim i powiecie wejherowskim jej wymowa to /ɛ̃/ .
