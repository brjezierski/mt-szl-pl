Älta
Älta – część `` tätortu '' Sztokholm , w regionie administracyjnym ( `` län '' ) Sztokholm ( gmina Nacka ) w Szwecji .
Älta jest położona w prowincji historycznej ( `` landskap '' ) Södermanland ; ok. 15 km na południowy wschód od centrum Sztokholmu w kierunku Tyresö .
Należy w ramach gminy Nacka do części gminy ( `` kommundel '' ) Älta .
Najnowsza historia Älty sięga początku XX w. , kiedy właściciel majątku ziemskiego Älta ( `` Älta gård '' ) rozpoczął sprzedaż działek przeznaczonych pod budowę domów letniskowych i jednorodzinnych .
Współcześnie w Älta dominuje zabudowa mieszana , z domami jedno- i wielorodzinnymi .
Z Älty pochodzi piosenkarka Petra Marklund .
W 2010 roku `` tätort '' Älta liczył 9989 mieszkańców .
