Äänekoski
Äänekoski – miasto w Finlandii , położone w centralnej części kraju , należące do prowincji Finlandia Zachodnia .
Położenie .
Miasto ma powierzchnię 885 km² .
Liczba mieszkańców wynosi 18 851 osób , zaś gęstość zaludnienia 21,3 os./km² ( dane z roku 2018 ) .
Dojazd samochodem do lotniska Jyväskylä zajmuje około 20 minut .
Gospodarka .
Przemysł w Äänekoski w dużej mierze opiera się na przetwarzaniu drewna , którego duże ilości zapewniają pobliskie lasy .
Rozwój gospodarczy miasta zapewniają też małe przedsiębiorstwa związane z nowymi technologiami , które są przyjazne dla środowiska .
Tutejsze młyny wykorzystywały kiedyś silny prąd rzeczny , co spowodowało osiedlenie się kolejnych partii osadników .
19 marca 2004 roku pod Äänekoski wydarzył się najtragiczniejszy w historii Finlandii wypadek drogowy , w którym zginęły 23 osoby .
