Édouard Manet
Édouard Manet ( ur .
23 stycznia 1832 w Paryżu , zm .
30 kwietnia 1883 tamże ) – francuski malarz , prekursor impresjonizmu , ilustrator .
Życiorys .
Urodził się jako pierworodny syn w zamożnej mieszczańskiej rodzinie .
Jego ojciec , Auguste Manet , był prawnikiem , pełnił obowiązki szefa gabinetu w Ministerstwie Sprawiedliwości .
Matka , Eugénie Désirée Fournier , pochodziła z rodziny arystokratycznej , spokrewnionej ze szwedzką rodziną królewską ( jej ojcem chrzestnym był król Szwecji Karol XIII ) .
Édouard miał również dwóch młodszych braci – Eugène ’ a i Gustave ' a .
W roku 1838 rozpoczął naukę w szkole w Vaugirard , jednakże nie przykładał się do nauki .
Z nadzieją na poprawę w 1842 roku został zapisany przez rodziców do Collège Rollin ( jednej z renomowanych szkół w Paryżu ) .
Tam Édouard poznał Antonina Prousta , swego późniejszego biografa .
Zmiana szkoły nie zmieniła nastawienia Maneta do nauki , jednakże w tym czasie wuj Édouarda , Edmond-Édouard Fournier , dostrzegł jego talent artystyczny .
Udzielał mu pierwszych lekcji rysunku , dla zapoznawania ze sztuką zabierał go do Luwru .
Ze względu na słabe wyniki nauki w College ojciec Édouarda zdał sobie sprawę , że musi porzucić marzenia o karierze prawniczej syna i pozwolić mu wybrać szkołę .
W roku 1847 Manet przystąpił do egzaminu do Wyższej Szkoły Morskiej , lecz bez powodzenia .
W celu zwiększenia swych szans zgłosił się na półroczny rejs statkiem dla kandydatów do szkoły morskiej .
Rejs ten jednak zniechęcił Maneta do kariery w marynarce i po kolejnym nieudanym egzaminie w roku 1849 udało mu się przekonać ojca do rozpoczęcia kariery malarza .
W styczniu 1850 rozpoczął terminowanie w pracowni Thomasa Couture ' a .
Po latach nauki chciał zaistnieć w środowisku artystycznym i szansą na to miało być zaprezentowanie swych prac na Salonie Paryskim .
Manet przez kilka ostatnich lat swojego życia cierpiał z powodu powikłań w wyniku syfilisu , na który zachorował , kiedy miał czterdzieści kilka lat .
