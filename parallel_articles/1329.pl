Opieńka bezpierścieniowa
Opieńka bezpierścieniowa ( `` Armillaria tabescens '' ( Scop . )
Emel ) – gatunek grzybów z rodziny obrzękowcowatych ( `` Physalacriaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Armillaria , Physalacriaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1772 r. Giovanni Antonio Scopoli , nadając mu nazwę `` Agaricus tabescens '' .
W 1916 r. mykolodzy Carleton Rea & amp ; John Ramsbottom uznali go za odmianę opieńki miodowej .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1921 r. L. Emel .
Synonimy naukowe :
Nazwę polską podał Władysław Wojewoda w 2003 r .
Morfologia .
Średnicy 2–10 cm , początkowo płasko półkulisty z garbem na środku , następnie płaski .
