Ô
Ô , Ôô – litera alfabetu łacińskiego używana w : języku francuskim , kaszubskim , portugalskim , wietnamskim , słowackim , neapolitańskim , a także w tzw .
ślabikŏrzowym szrajbōnku ( elementarzowej pisowni ) etnolektu śląskiego .
W języku francuskim i wietnamskim odpowiada głosce [ o ] , w śląskim Ô wymawia się jak [ wɔ ] , zaś w języku słowackim litera ta oznacza dyftong .
Język wietnamski jest językiem tonalnym , przy literze Ô może wystąpić jeden z pięciu znaków tonalnych :
Litery tej używa się także w języku słoweńskim na oznaczenie długiego otwartego [ ɔ : ] w przypadku występowania wyrazów homograficznych w celu uniknięcia niejednoznaczności , np .
