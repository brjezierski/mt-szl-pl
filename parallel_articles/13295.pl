Oblaty
Oblat ( , od = „ hostia ” ) – cienki , okrągły wafel wytwarzany z ciasta z wody , mąki pszennej i oleju , wypiekany w specjalnej formie .
Oblaty są tradycyjnym produktem kulinarnym znanym na Śląsku ( ołblaty ) , w Niemczech ( `` Oblaten '' ) , Czechach ( `` lázeňské oplatky '' ) i na Słowacji ( `` oblátky '' ) .
Śląskie oblaty .
Produkt o nazwie „ śląskie oblaty ” wpisano 17 maja 2010 na listę produktów tradycyjnych przez Ministerstwo Rolnictwa i Rozwoju Wsi .
Według oficjalnej specyfikacji , są to cienkie , lekkie , delikatne , w większości jasnozłociste wafle o średnicy 10–20 cm , grubości 1–3 mm , i z odciśniętym na powierzchni wzorem lub tekstem .
Ciasto z wody , mąki pszennej i oleju wylewa się na formy i wypieka w temperaturze ok. 180 °C .
Oprócz podstawowych składników , często dodawano cukier , mleko , wanilię lub cynamon .
Tradycja pieczenia oblatów na Śląsku sięga XIX wieku .
Stanowiły rodzaj popularnej , słodkiej przekąski a nawet słodyczy .
Tradycyjne formy do wypiekania oblatów noszą nazwy : presa , oblate żelazko , oblatnik , klyszcze .
Praktyka ich wytwarzania zaistniała dzięki umiejętnościom lokalnych mieszkańców pracujących w hutach i kuźniach .
