Ocean ( singel TVXQ )
OCEAN – trzydziesty siódmy singel południowokoreańskiego zespołu TVXQ , wydany w Japonii 12 czerwca 2013 roku przez Avex Trax .
Został wydany w trzech edycjach : regularnej CD , limitowanej CD+DVD oraz „ Bigeast ” .
Osiągnął 2 pozycję w rankingu Oricon i pozostał na liście przez 10 tygodni , sprzedał się w nakładzie egzemplarzy w Japonii .
Singel zdobył status złotej płyty .
