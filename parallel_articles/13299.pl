Oceania ( album The Smashing Pumpkins )
Oceania to ósmy studyjny album zespołu alt-rockowego The Smashing Pumpkins , wydany 19 czerwca 2012 roku przez EMI , Reprise Records oraz Martha 's Music .
Album jest częścią 44-piosenkowego box setu `` Teargarden by Kaleidyscope '' .
Wydano również wersję live albumu ( `` Oceania : Live in NYC '' ) 24 września 2013 .
