Odra ( Chorwacja )
Odra – wieś w środkowej Chorwacji , położona na terenie miasta Zagrzeb .
Miejscowość jest oddalona o około 5,5 km na zachód od miasta Velika Gorica i o około 9 km na południe od centrum Zagrzebia .
Odra w 2011 roku liczyła 1866 mieszkańców ( w czym 879 to mężczyźni a 987 – kobiety ) .
