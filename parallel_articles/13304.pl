Ogień ( film 1996 )
Ogień ( hindi : फायर , ang .
`` Fire '' ) to zrealizowany w 1996 roku indyjsko-kanadyjski dramat społeczny i rodzinny wyreżyserowany przez Deepę Mehtę , Induskę mieszkającą w Kanadzie .
Film jest pierwszym z filmów stworzonej przez nią trylogii .
Kontynuacją jego są `` Ziemia '' i `` Woda '' .
W rolach głównych Shabana Azmi i Nandita Das .
Kontrowersje .
Film wzbudził w Indiach liczne protesty .
Oskarżono go o oczernianie kultury indyjskiej .
Zabroniono wyświetlania , a kina , które wyświetlały , były demolowane przez protestujących ekstremistów hinduskich .
Oburzenie budził też przedstawiony w filmie wątek lesbijski połączony z wątkiem religijnym ( bohaterki noszą imiona kobiet czczonych w hinduizmie – Sita i Radha ..
Film był kręcony w Tadź Mahal ( w stanie Uttar Pradesh ) i w Nowym Delhi .
Ostatnia scena tam właśnie w świątyni Nizamudin .
Fabuła .
Sita ( Nandita Das ) nie rozumie chłodu , z jakim traktuje ją jej świeżo poślubiony mąż .
Nie wie , że urzeczony inną kobietą Jatin ( Javed Jaffrey ) poślubił ja tylko po to , by spełnić oczekiwania sparaliżowanej matki i starszego brata Ashoki ( Kulbhushan Kharbanda ) .
Rodzina potrzebuje dziedzica .
Sita ma im urodzić dziecko , które nigdy nie poczęło się w bezpłodnym małżeństwie Ashoki i Radhy ( Shabana Azmi ) .
Sita daremnie co wieczór czeka na powrót męża uciekającego do ukochanej kobiety .
Nie jest jednak w tym domu jedyną kobietą zaniedbywaną i nierozumianą przez męża .
Ashoka codziennie unika Radhy uciekając od niej w modlitwy , medytacje i rozmowy ze swoim guru .
Niespełniony jako ojciec rezygnuje z roli męża i kochanka Radhy .
Osamotnione , upokorzone odtrąceniem kobiety znajdują pocieszenie w sobie .
Wkrótce ich zażyłość staje się coraz intymniejsza .
Bezsilna i niema obserwuje to ich sparaliżowana teściowa ...
