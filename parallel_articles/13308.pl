Oksytania ( kraina historyczna )
Oksytania – nazwa regionu , w którym używa się języka oksytańskiego .
Zamieszkiwany jest przez Oksytańczyków .
Od 1 stycznia 2016 istnieje także region administracyjny Francji ( Oksytania ) , jednak obejmuje on mniejszy obszar .
