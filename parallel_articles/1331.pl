Harmonijka ustna
Harmonijka ustna – instrument muzyczny z grupy idiofonów języczkowych .
Dźwięk wydobywa się podczas ruchu powietrza w szczelinach , przy których znajdują się metalowe blaszki zwane stroikami ( pot .
`` języczkami '' ) , które wprawione w drgania wydają dźwięki zależne od ich długości .
Harmonijka ustna była pierwszym instrumentem Jimiego Hendrixa .
Najstarszym przodkiem harmonijki jest instrument sheng , skonstruowany przez starożytnych Chińczyków około 3000 roku p.n.e .
W Europie instrumenty stroikowe mają dużo skromniejszą historię .
Pod koniec XVIII w. petersburski konstruktor organów Kirschnigk , zainspirowany grą Johanna Wildego ( 1689–1762 ) na shengu zbudował organy , w których najwyższy rejestr wyposażył w stroiki przelotowe , zamiast tradycyjnych piszczałek .
W 1792 współpracownik Kirschnigka , szwedzki organmistrz Georg Christoffer Rackwitz zbudował orchestrion dla Georga Josepha Voglera ( 1749–1814 ) , kompozytora i wirtuoza organów .
Podczas podróży koncertowych po całej Europie Vogler przyczynił się do popularyzacji instrumentów ze stroikami przelotowymi .
W 1804 powstał pierwszy samodzielny instrument stroikowy – panharmonikon , na który pierwszy utwór skomponował Ludwig van Beethoven .
