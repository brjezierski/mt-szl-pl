Góry Opawskie
Góry Opawskie ( , ) ( 332.63 ) – mezoregion wchodzący w skład pasma górskiego Jesioników ( cz .
`` Jeseníky '' ) , w Sudetach Wschodnich , w Czechach i częściowo w Polsce z najwyższym szczytem : Příčný vrch ( pol .
`` Góra Poprzeczna '' ) , położonym na terenie Czech .
Za najwyższą górę polskiej części Gór Opawskich przyjmuje się Biskupią Kopę .
Położenie .
W Czechach Góry Opawskie graniczą : od zachodu z Przedgórzem Paczkowskim ( cz .
`` Žulovská pahorkatina '' ) i Górami Złotymi ( cz .
`` Rychlebské hory '' ) , od południowego zachodu z Wysokim Jesionikiem ( cz .
`` Hrubý Jeseník '' ) i od południa z Niskim Jesionikiem ( cz .
`` Nízký Jeseník '' ) .
Natomiast w Polsce Góry Opawskie ograniczone są krętą linią : Głuchołazy – Moszczanka – Łąka Prudnicka – Prudnik – Trzebina – Skrzypiec – Krzyżkowice – Dobieszów – Mokre – Zopowy – Zubrzyce – Lewice – Michałkowice – Bliszczyce .
Graniczą tu z Płaskowyżem Głubczyckim , a na północnym zachodzie z Przedgórzem Paczkowskim .
Masyw Hranicznego Wierchu oddzielony jest od pozostałej polskiej części Gór Opawskich granicą państwową , która ma tu wyjątkowo zawikłany przebieg .
Jest to najdalej na wschód położony na obszarze Polski fragment Sudetów .
Podział Gór Opawskich .
Podział fizycznogeograficzny Sudetów Wschodnich ( w tym i Gór Opawskich ) przy znacznej odrębności metodologii stosowanej w Polsce oraz w Czechach i jednocześnie nieuwzględnianiu kryteriów genetycznych geomorfologiczno-geologicznych , budzi wśród geomorfologów spore kontrowersje i rozbieżności .
Efektem prowadzonych sporów są korekty granic Gór Opawskich , zarówno części polskiej , jak i czeskiej .
Biorąc pod uwagę położenie , pochodzenie i strukturę rzeźby terenu oraz budowę geologiczną , a także warunki ekologiczno-krajobrazowe i formy antropopresji Góry Opawskie według polskich geomorfologów ( dr Krzysztof Badora ) podzielono na 7 mikroregionów fizyczno-geograficznych , z których 3 są częściowo zlokalizowane na terenie Polski :
