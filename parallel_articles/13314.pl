Najwyższy masyw Biskupiej Kopy ogranicza od północy przełomowa dolina Złotego Potoku , a od wschodu obniżenie w którym rozłożyła się Wieszczyna ( Nowa Wieś ) .
Tu znajduje się najwyższy szczyt Gór Opawskich leżący na terenie Polski , Biskupia Kopa , na której istnieje wieża widokowa a do 2007 funkcjonowało turystyczne przejście graniczne .
