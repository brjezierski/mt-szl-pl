Opolanie ( plemię )
Opolanie – plemię słowiańskie należące do grupy Słowian zachodnich .
Zamieszkiwało tereny nad górną Odrą , głównym ich grodem było Opole .
Wzmiankowani w połowie IX wieku w tzw .
Geografie Bawarskim .
Wymieniani wśród 6 innych plemion śląskich , obok Dziadoszan , Ślężan , Gołęszyców , Bobrzan , Trzebowian i Głupczyców .
Według Geografa Bawarskiego władali 20 grodami na Górnym Śląsku .
Przypuszcza się , że ewentualnym miejscem kultu plemiennego Opolan mogła być Góra Świętej Anny .
Terytoria Opolan zostały prawdopodobnie włączone do kraju czeskich Przemyślidów w poł .
X w .
Kilkadziesiąt lat później kraj Opolan zajął Mieszko I włączając go do tworzonego państwa polskiego .
Nazwa `` Opolanie '' pochodzi od słowiańskiego słowa `` opole '' , co znaczyło pewną formę samorządu w grodzie .
( Od plemienia Opolanie pochodzi nazwa dzisiejszego miasta – Opola ) .
W skład `` opola '' wchodziło 10 osób – członków starszyzny plemiennej , które zarządzały grodem .
