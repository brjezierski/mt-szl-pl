Położenie historyczne .
Większość obszarów województwa stanowi zachodnią część Górnego Śląska , natomiast cały region jest często określany Śląskiem Opolskim , nie do końca poprawnie , gdyż północno-wschodnie skrawki województwa ( gmina Praszka i gmina Rudniki ) nie leżą na historycznym Śląsku , lecz w Wielkopolsce ( ziemia wieluńska ) .
Zachodnia część obecnego województwa obejmują tereny historycznego Dolnego Śląska : większa część ziemi brzeskiej oraz ziemię namysłowską , które do 1950 roku należały do województwa wrocławskiego .
Również takie miasta jak Nysa , Grodków , Otmuchów , Paczków i Głuchołazy położone są na terenach historycznego Dolnego Śląska ( dawne księstwo nyskie ) .
Prudnik i jego najbliższe okolice w średniowieczu stanowiły część Moraw ( do 1337 roku ) .
W południowej części województwa , w okolicy Kietrza znajduje się enklawa morawska .
