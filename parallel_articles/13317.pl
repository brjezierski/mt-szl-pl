Województwo opolskie
Województwo opolskie – jedno z 16 województw w Polsce .
Obejmując obszar o powierzchni 9412 km² jest obecnie najmniejszym województwem w Polsce .
Według danych z 30 czerwca 2020 r. było też województwem z najmniejszą liczbą mieszkańców – 980 771 mieszkańców .
Siedzibą władz województwa jest Opole .
Historia .
Pierwsze województwo opolskie zostało utworzone w 1950 r. , w 1975 odłączone od niego zostały powiat raciborski i powiat oleski ; ten ostatni , powiększony terytorialnie , powrócił w granice województwa w wyniku reformy administracyjnej w 1999 r .
Województwo opolskie utworzono w 1950 roku na mocy ustawy z dnia 28 czerwca 1950 roku , w wyniku podziału województwa śląsko-dąbrowskiego na województwo katowickie i opolskie .
Do województwa opolskiego dołączono powiat brzeski i powiat namysłowski z województwa wrocławskiego .
Nowe województwo , liczące 9506 km² , było najmniejszym w ówczesnej Polsce .
Według danych z 1950 ponad 50 % ówczesnych mieszkańców stanowili autochtoni , około 25 % mieszkańców województwa stanowili Kresowianie .
W 1975 roku w wyniku reformy administracyjnej powstało nowe województwo opolskie : powiat raciborski przyłączono do województwa katowickiego , a niemal cały powiat oleski do nowo utworzonego województwa częstochowskiego ( 2 z 8 jednostek pozostały w województwie opolskim ) .
Od 1999 roku , wobec likwidacji województwa częstochowskiego , powiat oleski jest ponownie częścią woj .
