Język osmański
Język osmański , język osmańskoturecki , język ottomański ( lub , osm .
`` lisân-ı Osmânî '' ) – oficjalny język Imperium Osmańskiego , wywodzący się z grupy języków turkijskich .
Zapisywany był zmodyfikowanym alfabetem perso-arabskim .
Język ten powstał w wyniku długiej , obejmującej kilka stuleci , koegzystencji trzech głównych języków używanych przez elity rządzące państwem osmańskim .
Na język turecki , jako ojczysty dla większości mieszkańców państwa , nawarstwiały się silne wpływy perskiego – głównego języka kultury i literatury narodów Bliskiego Wschodu – oraz arabskiego , jako języka religii .
