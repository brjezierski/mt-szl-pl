Nazwa .
Miejscowość w historii miała wiele nazw w językach czeskim , polskim i niemieckim .
Niemiecki nauczyciel Heinrich Adamy w swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu jako najstarszą nazwę miejscowości wymienia zlatynizowane nazwy `` Ossoblavia '' i `` Ossoblow '' , podając jej znaczenie `` Von der Ossa umgeben '' .
Pierwotna nazwa została później przez Niemców zgermanizowana na `` Hotzenplotz '' , tracąc swoje pierwotne znaczenie .
W roku 1613 śląski regionalista i historyk Mikołaj Henel z Prudnika wymienił miejscowość w swoim dziele o geografii Śląska pt .
`` Silesiographia '' podając jej łacińską nazwę : `` Osoblavia '' .
