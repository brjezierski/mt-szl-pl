Arne Næss
Arne Næss ( ur .
27 stycznia 1912 , zm .
12 stycznia 2009 w Oslo ) – norweski filozof i działacz społeczny , twórca .
Alpinista .
Życiorys .
Næss ukończył filozofię na Uniwersytecie w Oslo w roku 1933 , następnie studiował matematykę i astronomię w Paryżu i Wiedniu .
Był członkiem Koła Wiedeńskiego .
Doktoryzował się w Oslo w roku 1936 na podstawie rozprawy poświęconej epistemologii poznania naukowego .
W roku 1939 , mając 27 lat , został najmłodszym profesorem macierzystego uniwersytetu i jedynym profesorem filozofii w Norwegii .
Zajmował się filozofią nauki i języka , logiką , semantyką i psychoanalizą , rozwijał myśl Spinozy i Gandhiego .
Jest autorem licznych książek i artykułów z dziedziny filozofii , ekologii i logiki , z których wiele stało się bestsellerami i podręcznikami akademickimi .
Założył interdyscyplinarne pismo filozoficzne „ Inquiry ” .
W latach 60. i 70 .
XX wieku pod wpływem m.in .
książki Rachel Carson „ ” stworzył teoretyczne zręby nurtu ekofilozoficznego „ głęboka ekologia ” .
Næss odrzucał antropocentryzm ; różnorodność biologiczna była dla niego wartością samą w sobie .
W roku 1970 zrezygnował z pracy na uniwersytecie i poświęcił się działalności w ruchu ekologicznym .
