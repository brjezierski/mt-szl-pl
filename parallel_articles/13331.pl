Østre Toten
Østre Toten ( ) – norweska gmina leżąca w regionie Oppland .
Jej siedzibą jest miasto Lena .
Østre Toten jest 190. norweską gminą pod względem powierzchni .
Demografia .
Według danych z roku 2005 gminę zamieszkuje 14 604 osób , gęstość zaludnienia wynosi 26,03 os./km² .
Pod względem zaludnienia Østre Toten zajmuje 66. miejsce wśród norweskich gmin .
