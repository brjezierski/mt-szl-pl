Uście nad Łabą
Uście nad Łabą ( , ) – miasto statutarne w północno-zachodnich Czechach , stolica kraju usteckiego , okręgu terytorialnego kraj północnoczeski i powiatu Uście nad Łabą .
Według danych na 2020 rok powierzchnia miasta wynosiła 93,95 km² , a liczba jego mieszkańców 92 716 osób ( dziewiąte pod względem liczby ludności miasto w Czechach ) .
W języku czeskim słowo oznacza ujście rzeki .
W tym przypadku odnosi się do ujścia rzeki Bíliny do Łaby , które znajduje się nieopodal centrum miasta .
Położenie .
Uście nad Łabą leży u podnóża Rudaw .
