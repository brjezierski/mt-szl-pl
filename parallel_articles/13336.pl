Ciapkapusta
Ciapkapusta – śląskie danie przygotowywane z purée ziemniaczanego i kapusty , zwykle kiszonej .
Do ciapkapusty dodaje się podsmażony i pokrojony w kostkę boczek , kiełbasę lub zrumienioną cebulę .
Potrawa ta zwykle podawana jest z mięsem ( często z żeberkami ) .
Inne nazwy to : ciaperkapusta , ciaperkrałt , ciaprówa , pańczkrałt , ciamkrałt lub panczkrałt ( Opolszczyzna : region opolski , brzeski i krapkowicki ) , klaplastra , ćpocha .
