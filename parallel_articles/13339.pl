Ciechanów
Ciechanów – miasto położone w województwie mazowieckim , w Polsce .
Miejscowość jest siedzibą powiatu ciechanowskiego .
Leży nad rzeką Łydynią , ok. 100 km na północ od Warszawy .
Był miastem królewskim Korony Królestwa Polskiego .
Miejsce obrad sejmików ziemskich ziemi ciechanowskiej od XVI wieku do pierwszej połowy XVIII wieku .
Ciechanów należał do starostwa ciechanowskiego w 1617 roku .
Miasto rządowe Królestwa Kongresowego , położone było w 1827 roku w powiecie przasnyskim , obwodzie przasnyskim województwa płockiego .
W latach 1975–1998 miasto było stolicą województwa ciechanowskiego .
