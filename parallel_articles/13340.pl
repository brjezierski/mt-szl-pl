Ciechocinek ( stacja kolejowa )
Ciechocinek – nieczynna stacja kolejowa w Ciechocinku , w województwie kujawsko-pomorskim , w Polsce .
Została zaprojektowana przez Czesława Domaniewskiego i wybudowana w 1902 roku .
Dworzec jest nieczynny od 2015 roku .
Historia .
Dworzec został wzniesiony w latach 1901–1902 na podstawie projektu Czesława Domaniewskiego .
W trakcie II wojny światowej w styczniu 1945 roku budynek został spalony przez hitlerowców .
Obecnie gmach przykryty jest dachem o kształcie kopuły , inny jest też daszek przy wyjściu .
Zostały przekształcone środkowe i boczne ryzality .
Dworzec jest nieczynny od 2015 roku i obecnie w budynku stacji funkcjonuje sklep ze starociami oraz restauracja .
Pociągi .
11 grudnia 2011 roku wszystkie pociągi z Ciechocinka zostały wstrzymane .
21 czerwca 2014 roku prywatny przewoźnik Arriva RP wznowił na okres do 28 września 2014 roku połączenia kolejowe w soboty i niedziele ze stacją Toruń Wschodni .
W 2018 roku pojawił się pomysł przywrócenia ruchu kolejowego , który miała obsługiwać firma SKPL , ale pomysł został odrzucony .
