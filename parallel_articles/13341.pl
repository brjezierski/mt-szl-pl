Ciekoty
Ciekoty – wieś w Polsce położona w województwie świętokrzyskim , w powiecie kieleckim , w gminie Masłów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
Położenie .
Wieś położona jest w Dolinie Wilkowskiej w Górach Świętokrzyskich .
Znajduje się u stóp Pasma Masłowskiego , na północ od szczytu Radostowej .
Przez Ciekoty przepływa rzeka Lubrzanka .
Znajduje się tu zbiornik retencyjny wykorzystywany do celów rekreacyjnych .
Dojazd z Kielc zapewniają autobusy komunikacji miejskiej linii 10 oraz 38 .
