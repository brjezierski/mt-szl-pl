Cielemęc
Cielemęc – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Zbuczyn .
Wieś szlachecka `` Cielemęcz '' położona była w drugiej połowie XVI wieku w ziemi łukowskiej województwa lubelskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Stanisława i Aniołów Stróżów w Zbuczynie .
