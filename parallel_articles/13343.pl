Cieleśnica
Cieleśnica – wieś w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Rokitno .
Wierni Kościoła rzymskokatolickiego należą do parafii Trójcy Świętej w Janowie Podlaskim .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
Założenie folwarku .
Historyczne korzenie miejscowości sięgają już XV wieku , kiedy to ziemie te znajdowały się w posiadaniu rodziny Andruszkiewiczów .
W 1526 roku majątek przeszedł w ręce rodziny Cieleśnickich , która założyła folwark i dała początek nazwie wsi .
Na przestrzeni wieków folwark zmieniał swoich właścicieli i w roku 1630 został zakupiony przez Aleksandra Ludwika Radziwiłła ( ówczesnego stolnika Wielkiego Księstwa Litewskiego ) oraz włączony do hrabstwa bialskiego .
