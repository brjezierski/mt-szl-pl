Cielęta
Cielęta ( niem .
Zeland ) – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie brodnickim , w gminie Brodnica .
W latach 1975–1998 miejscowość należała administracyjnie do województwa toruńskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyły 614 mieszkańców .
Są trzecią co do wielkości miejscowością gminy Brodnica .
Infrastruktura .
Znajduje się tutaj nadajnik radiowy Radia PiK dla Brodnicy pracujący na częstotliwości 106,9 MHz ( 10.00 H : polaryzacja pozioma ) , jak również parafia pod wezwaniem św .
