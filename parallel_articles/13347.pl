Cieszanów ( gmina )
Cieszanów – gmina miejsko-wiejska w województwie podkarpackim , w powiecie lubaczowskim .
W latach 1975–1998 gmina położona była w województwie przemyskim .
Siedziba gminy to Cieszanów .
Według danych z 30 czerwca 2004 gminę zamieszkiwało 7260 osób .
Położenie .
Gmina Cieszanów położona jest w północnej części Płaskowyżu Tarnogrodzkiego , jednego z mezoregionów Kotliny Sandomierskiej , w obrębie Garbu Tarnogrodzkiego , na północ od rzeki Lubaczówki .
Granica między Płaskowyżem Tarnogrodzkim a sąsiadującym z nim Roztoczem przebiega niedaleko krańców gminy .
