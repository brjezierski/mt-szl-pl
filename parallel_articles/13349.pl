Nazwa .
Po raz pierwszy Cieszyn wzmiankowany został w 1155 jako `` Tescin '' , w bulli protekcyjnej papieża Hadriana IV dla biskupa wrocławskiego Waltera , w której wymieniono Cieszyn jako jeden z grodów kasztelańskich .
Z kolei w 1223 miejscowość wzmiankowano jako `` Tessin '' .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Teschin '' .
Księga ta wymienia również Kalembice , które w 1973 zostały wchłonięte przez miasto , w formie `` Chalambyci '' .
W roku 1613 śląski regionalista i historyk Nicolaus Henel von Hennenfeld z Prudnika wymienił miejscowość w swoim dziele o geografii Śląska pt .
`` Silesiographia '' podając jej łacińskie nazwy : `` Teschena '' , `` Tischena '' , `` Cessinum '' , `` Cissenum '' .
Demografia .
Według danych GUS na 1 stycznia 2019 Cieszyn liczył 33 485 mieszkańców z czego 52,8 % stanowią kobiety , a 47,2 % mężczyźni co daje 153. miejsce w Polsce pod względem liczby mieszkańców oraz 2172 pod względem zajmowanej powierzchni .
Liczba urodzonych dzieci wyniosła 1371 .
Gęstość zaludnienia wynosi 1170 os./km² .
