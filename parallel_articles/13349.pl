Cieszyn
Cieszyn ( , , ) – miasto w południowej Polsce nad rzeką Olzą , na granicy z Czechami , w województwie śląskim , siedziba władz powiatu cieszyńskiego .
Dzieje Cieszyna sięgają IX w. i wiążą się z grodem słowiańskiego plemienia Gołęszyców .
Cieszyn otrzymał prawa miejskie w 1217 roku .
Od końca XIII w. był centrum Księstwa Cieszyńskiego .
Współczesne administracyjne pojęcie „ miasto Cieszyn ” odnosi się do prawobrzeżnej części , będącego pod względem przestrzennym i społecznym jedną całością , ośrodka miejskiego , którego część lewobrzeżna należy do Czech i nosi oficjalną nazwę Czeski Cieszyn .
W przeciwieństwie do Czeskiego Cieszyna , dzielnice prawobrzeżne są dzisiaj jednolite etnicznie – ponad 96 % ludności deklaruje narodowość polską .
Charakterystyczna jest natomiast różnorodność religijna z wyznaniami rzymskokatolickim i luterańskim jako dominującymi .
Według danych na 31 grudnia 2021 r. polski Cieszyn liczy 33 601 mieszkańców , jego powierzchnia wynosi natomiast 28,7 km² .
