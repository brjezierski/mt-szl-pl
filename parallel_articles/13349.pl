Położenie .
Cieszyn znajduje się w południowej części Polski , w centrum na 18º 35 ' 50 '' długości geograficznej wschodniej i 49º 43 ' 08 '' szerokości geograficznej północnej .
Pod względem administracyjnym leży w południowej części województwa śląskiego , w powiecie cieszyńskim .
Graniczy od północy z gminą Hażlach ( z Pogwizdowem , Hażlachem i Zamarskami ) , od północnego wschodu z gminą Dębowiec ( z Gumnami i Ogrodzoną ) , od wschodu i południa z gminą Goleszów ( z Bażanowicami , Dzięgielowem i Puńcowem ) .
Zachodnia granica miasta o długości ok. 9,7 km pokrywa się z granicą polsko-czeską , za którą leży miasto Czeski Cieszyn .
Miasto leży na Pogórzu Śląskim , nad Olzą .
Cieszyn jest stolicą krainy historyczno-geograficznej Śląska Cieszyńskiego .
W latach 1975–1998 miasto administracyjnie należało do województwa bielskiego .
