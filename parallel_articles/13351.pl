Gwara cieszyńska
Gwara cieszyńska ( ciesz .
`` po naszymu '' , `` cieszyńsko rzecz '' , w literaturze czeskiej także dialekt śląsko-polski ( cz .
`` slezskopolská nářečí '' ) – jedna z gwar śląskich , zaliczana do dialektu śląskiego lub do grupy śląskiej dialektów języka czeskiego , razem z dialektem laskim .
Charakteryzuje się dużymi wpływami czeskimi , niemieckimi , a nawet wołoskimi i słowackimi .
Jest używana po obu stronach granicy polsko-czeskiej .
Polscy i czescy językoznawcy spierają się w kwestii klasyfikacji gwary : Polacy uważają ją za część dialektu śląskiego języka polskiego , Czesi wykazują jej podobieństwo do gwar laskich i morawskich .
Na Zaolziu jest używana głównie ( choć nie tylko ) przez mniejszość polską i ulega silnym wpływom języka czeskiego .
Po polskiej stronie granicy ulega silnym wpływom polszczyzny standardowej .
Pisarzami tworzącymi w gwarze cieszyńskiej byli : Aniela Kupiec , Emilia Michalska , Władysław Młynek , Mieczysław Peterek , Karol Piegza , Jerzy Rucki , Maria Szturc i Adam Wawrosz .
Pochodzenie .
Dialekt Śląska Cieszyńskiego , według polskich językoznawców , wyewoluował ze staropolszczyzny , zachowując do dzisiejszych czasów wiele cech języka staropolskiego , takich jak słownictwo , składnia .
Duży wpływ na kształtowanie się gwary miały też i mają języki : czeski , niemiecki , polski standardowy , a od niedawna także język słowacki .
Według czeskich językoznawców gwara cieszyńska jest spokrewniona głównie z gwarami laskimi i morawskimi i wykazuje większe podobieństwo do języka czeskiego niż do współczesnej polszczyzny .
Obszar użycia .
Gwara cieszyńska jest używana po obu stronach Olzy , zarówno po czeskiej , jak i polskiej .
Na Zaolziu używana jest przede wszystkim przez mniejszość polską .
Używana jest głównie na wsiach i w mniejszych miejscowościach .
W miastach częściej używa się języka ogólnopolskiego .
Gwarze cieszyńskiej lepiej powodzi się po stronie czeskiej , powodem tego jest , że gwara cieszyńska bliższa jest językowi polskiemu , przez co dochodzi do głównie fonetycznej integracji z polszczyzną .
Na Śląsku Cieszyńskim określenie `` po naszymu '' ( pol .
„ po naszemu ” ) odnosi się zazwyczaj do cieszyńskiej odmiany dialekty śląskiego , co jest spowodowane nie tylko zróżnicowaną mową , w szczególności w porównaniu do dialektów gliwickich z północnej części Górnego Śląska , ale i kilkusetletnim odgałęzieniem kulturowym od reszty Górnego Śląska .
