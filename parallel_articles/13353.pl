Ciszyca-Kolonia
Ciszyca-Kolonia – wieś sołecka w Polsce położona w województwie świętokrzyskim , w powiecie opatowskim , w gminie Tarłów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnobrzeskiego .
Według spisu powszechnego z roku 1921 Ciszyca kolonia ( obok Ciszycy Dolnej , Ciszycy Górnej .
Ciszycy Przewozowej ) posiadała 75 domów i 444 mieszkańców w tym : 9. ewangelików , 5. innego chrześcijańskiego wyznania i 8. wyznania mojżeszowego - wszyscy deklarowali narodowość polską
Wierni kościoła rzymskokatolickiego należą do parafii Świętej Trójcy w Tarłowie .
