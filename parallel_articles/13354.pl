Cisie ( przystanek kolejowy )
Cisie – przystanek kolejowy w gminie Halinów , w województwie mazowieckim , w Polsce .
W roku 2017 stacja obsługiwała 500–699 pasażerów na dobę .
Dane ogólne .
Przystanek posiada dwa perony krawędziowe standardowej wysokości ( wysokie ) , wykończone w całości elementami betonowymi ; oraz murowany budynek .
Perony nie znajdują się naprzeciwko siebie i są oddzielone przejazdem z automatycznymi rogatkami .
Połączenia pasażerskie .
Na przystanku zatrzymują się wyłącznie pociągi osobowe obsługujące wszystkie dostępne przystanki .
