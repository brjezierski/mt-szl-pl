Cisie-Zagrudzie
Cisie-Zagrudzie – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Kotuń .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Przez miejscowość przebiega droga gminna Żelków-Kolonia – Kotuń .
Opis .
Cisie w 1783 roku są własnością Dominika Przeździeckiego starosty mińskiego .
Majątek w 1804 roku kupuje generał artylerii Teodor Potocki .
21 lipca 1817 roku generałowi się rodzi syn Pantaleon Ignacy , ojciec jego umiera w 1839 roku .
W 1846 roku Pantaleon Potocki przystępuje do spisku powstańczego , jednak zostaje złapany i trafia do cytadeli warszawskiej , potem zostaje stracony na rynku miejskim w Siedlcach .
Potockiego majątek w Cisiu zostaje skonfiskowany .
W 1872 roku właścicielem dóbr zostaje hrabia Zygmunt Twarowski , który rozpoczyna budowę eklektycznego pałacu z wieżą i zakłada 2,5 hektarowy park .
Chcąc dokończyć wystawną budowę , parceluje część majątku , sprzedaje ziemię chłopom i tak powstaje Zagrudzie .
W 1913 roku Twarowscy nie wykończywszy jeszcze pałacu , sprzedają majątek Zygmuntowi Fortunatowi Starzyńskiemu , który kończy w końcu jego budowę .
Kolejnymi właścicielami majątku zostają w 1921 r. Jan Niemira , w 1922 r. Stanisław Klome , a w 1927 r. Józef Maria Mielżyński .
Ostatnim właścicielem rezydencji od 1929 r. do października 1944 r. był baron Jerzy Konopka .
Po wojnie w pałacu mieściła się szkoła i mieszkania dla nauczycieli .
