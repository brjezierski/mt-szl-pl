Ciemność
Ciemność – w potocznym rozumieniu jest to brak dostępu światła widzialnego .
Nauka .
Przy zdefiniowaniu światła , jako obejmującego pełne spektrum promieniowania elektromagnetycznego , ciemność rozumie się jako obszar , w którym nie występuje promieniowanie elektromagnetyczne .
Tak określonej ciemności nie można utworzyć , gdyż wszystkie ciała wypromieniowują ciepło w postaci promieniowania podczerwonego .
Doskonała ( absolutna ) ciemność jest teoretycznie możliwa w temperaturze zera bezwzględnego , co wynika wprost z prawa Stefana-Boltzmanna .
Mitologia .
W zachodniej tradycji ciemność łączona z czarną barwą jest utożsamiana ze złem , piekłem , zaświatami .
