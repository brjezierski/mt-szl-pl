Ćwiercie
Ćwiercie ( dodatkowa nazwa w j. niem .
Schwärze ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Walce .
Na terenie wsi mieszka 1 % mieszkańców gminy , a zajmuje ona 0,9 % powierzchni gminy tj .
0,62 km² , co czyni wioskę , najmniejszym sołectwem w gminie Walce .
Położenie .
Wieś znajduje się przy drodze z Kromołowa do Rozkochowa .
Nazwa .
Miejscowość miała w swej historii kilka nazw , takich jak : Ćwierci , Ćwiercice , Schwärze ,
Historia .
W 1568 roku , Ćwiercie zostają kupione , wraz z niedalekim Rozkochowem , przez hrabiego Hansa von Redern .
Pod koniec XVIII wieku , właścicielami wsi są panowie Pücklerowie z Rozkochowa , a następnie , po sprzedaży , wioska trafia w ręce grafów Seherr-Thoss .
