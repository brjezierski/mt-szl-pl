Cierpigórz ( przystanek kolejowy )
Cierpigórz – przystanek na linii kolejowej Siedlce – Czeremcha , położony w Cierpigórzu , w województwie mazowieckim , w Polsce .
Leży pomiędzy Cierpigórzem a Zawadami .
W roku 2017 przystanek obsługiwał 0–9 pasażerów na dobę .
Pomieszczenie dla pasażerów stanowi stara drewniana szopa .
