Ciąg ( jeździectwo )
Ciąg – chód boczny , w którym koń porusza się jednocześnie do przodu i w bok , przy czym jest wygięty w kierunku do linii po której się porusza .
Ciąg może być wykonywany w stępie , kłusie i galopie .
Rozróżnia się ciąg w lewo i ciąg w prawo .
Jest ruchem wykonywanym na przekątnej czworoboku .
W trakcie wykonywania ćwiczenia można go pomylić z ustępowaniem od łydki , jednak w odróżnieniu od niego w przypadku ciągu koń ustawiony jest do kierunku ruchu .
Aby poprawnie wykonać ciąg zwierzę musi być przynajmniej częściowo zebrane .
