Čakovec
Čakovec ( węg .
`` Csáktornya '' , niem .
`` Csakathurn '' lub `` Tschakathurn '' ) – miasto w Chorwacji , w żupanii medzimurskiej , siedziba miasta Čakovec .
Leży pomiędzy rzekami Murą i Drawą .
W 2011 roku liczył 15 147 mieszkańców .
Większość populacji miasta to etniczni Chorwaci i katolicy , natomiast mniejszość stanowią Serbowie , Węgrzy , Słoweńcy i Cyganie .
W 1910 roku spis powszechny wykazał , że w miejscowości mieszkało 5213 osób , w tym 2433 Węgrów , 2404 Chorwatów i 251 Niemców .
W latach 1941–1945 było pod okupacją węgierską .
W mieście rozwinął się przemysł tekstylny , spożywczych , materiałów budowlanych oraz hutniczy .
