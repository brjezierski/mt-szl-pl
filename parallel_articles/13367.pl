Čapljina
Čapljina – miasto w południowej Bośni i Hercegowinie , w Federacji Bośni i Hercegowiny , w kantonie hercegowińsko-neretwiańskim , siedziba miasta Čapljina .
Leży nad Neretwą , nieopodal granicy z Chorwacją .
W 2013 roku liczyło 5774 mieszkańców , z czego większość stanowili Chorwaci .
