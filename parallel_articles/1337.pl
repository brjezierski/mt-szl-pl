Języczek siatkowany
Języczek siatkowany ( `` Arrhenia retiruga '' ( Bull . )
Redhead ) – gatunek grzybów z rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Arrhenia , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1791 r. Jean Baptiste François Bulliard , nadając mu nazwę `` Helvella retiruga '' .
Jak sztuczna , oparta tylko na zewnętrznym podobieństwie była to klasyfikacja , świadczy fakt , że `` Helvella '' jest przedstawicielem typu workowców , tymczasem gatunki rodzaju `` Arrhenia '' należą do typu podstawczaków .
Później gatunek ten zaliczany był do różnych innych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Scott Alan Redhead w 1984 r .
