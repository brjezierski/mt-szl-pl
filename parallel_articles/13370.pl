Černova
Černova ( słow .
`` Černová '' ; węg .
`` Csernova '' ) – obecnie jedna z pięciu dzielnic Rużomberku , dawniej samodzielne miasto w Kraju Żylińskim .
W 1907 roku doszło tam do antywęgierskiego wystąpienia , które zakończyło się zbrojną pacyfikacją ludności ( Tragedia w Černovej ) .
Historia .
Pierwsza pisemna wzmianka o Černovej pochodzi z 1376 roku .
Liczbę ludności po raz pierwszy określono w 1766 roku – wieś miała wtedy 268 obywateli .
W 1825 – już 860 .
Černova jest dla Słowaków symbolem walki o tożsamość narodową .
27 października 1907 roku doszło tam do rozlewu krwi .
Obywatele wsi z pomocą Andreja Hlinki wybudowali kościół .
Chcieli aby to właśnie ten ksiądz dokonał poświęcenia .
Hlinka jednak przebywał w tym czasie w więzieniu , więc władze węgierskie przysłały swoich księży , którzy mieli poświęcić budynek .
Mieszkańcy żądali aby z obrzędem wstrzymano się do czasu powrotu Hlinki i stawili opór wobec przysłanej delegacji .
Węgierscy żandarmi przełamali go jednak .
Zginęło 15 osób , 60 zostało rannych .
W 1908 roku w związku ze sprawą oskarżono 50 osób , z których 40 dostało wyroki więzienia lub wysokiej grzywny .
