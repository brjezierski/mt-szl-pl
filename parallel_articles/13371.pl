Česká Lípa
Česká Lípa ( niem .
`` Böhmisch Leipa '' ) – miasto w Czechach , w kraju libereckim .
Według danych z 31 grudnia 2016 powierzchnia miasta wynosiła 6324 ha , a liczba jego mieszkańców to 37 201 osób .
Miasto jest dużym regionalnym ośrodkiem edukacyjnym – znajduje się tu 10 szkół poziomu ponadpodstawowego .
Pierwsza wzmianka o mieście pochodzi z roku 1263 , zniszczona na skutek pożaru w 1787 .
W mieście rozwinął się przemysł środków transportu , metalowy , włókienniczy oraz drzewny .
