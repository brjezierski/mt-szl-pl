Česká Třebová ( stacja kolejowa )
Česká Třebová – stacja kolejowa w miejscowości Česká Třebová , w kraju pardubickim , w Czechach na adresie náměstí Jana Pernera 579 .
Położona jest na wysokości 385 m n.p.m .
Jest to ważna stacja węzłowa na linii do Pragi , Brna i Ołomuńca .
Obecny budynek dworca został wybuowany w 1924 .
