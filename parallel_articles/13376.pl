Čáslav ( stacja kolejowa )
Čáslav – stacja kolejowa w miejscowości Čáslav , w kraju środkowoczeskim , w Czechach .
Jest to ważna stacja węzłowa o znaczeniu regionalnym .
Znajduje się na wysokości 245 m n.p.m .
Stacja jest wyposażona w poczekalnię , kasy biletowe , na których można zakupić bilety na pociągi krajowe i międzynarodowe oraz zarezerwować miejsce w pociągu .
