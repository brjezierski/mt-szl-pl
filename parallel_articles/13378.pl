Đà Lạt
Đà Lạt ( wym .
; `` Dalat '' ) – miasto ( kurort ) w południowym Wietnamie , centrum administracyjne prowincji Lâm Đồng .
Liczba mieszkańców wynosi około 133 695 ( 2008 ) .
Położone w Górach Annamskich , w kotlinie na wysokości 1220 m n.p.m. , nad rzeką Cam Ly .
Zamieszkana przez wietnamskich górali ( Degar ) kotlina została odkryta w 1893 roku przez francuskiego lekarza Aleksandra Yersina jako doskonałe miejsce na uzdrowisko dla francuskich pacjentów .
