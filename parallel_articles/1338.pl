Języczek ciemny
Języczek ciemny ( `` Arrhenia umbratilis '' ( Fr . )
Redhead , Lutzoni , Moncalvo & amp ; Vilgalys ) – gatunek grzybów z rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Arrhenia , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1828 r. Elias Fries , nadając mu nazwę `` Agaricus umbratilis '' .
Później gatunek ten zaliczany był do różnych innych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę , nadali mu Scott Alan Redhead , François M. Lutzoni , Jean-Marc Moncalvo i Rytas J. Vilgalys w 2002 r .
