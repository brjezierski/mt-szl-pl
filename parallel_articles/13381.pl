Izmir
Izmir ( ; hist .
`` Smyrna '' , ) – miasto portowe w zachodniej Turcji nad Zatoką Izmirską , stolica prowincji ( ilu ) Izmir .
Trzecie miasto pod względem wielkości w Turcji , liczące ponad 4,2 mln mieszkańców .
Ważny ośrodek handlowy i przemysłowy Turcji : huta żelaza , rafineria ropy naftowej , zakłady tytoniowe , włókiennicze i cementowe .
Drugi port handlowy po Stambule .
W Izmirze odbywają się największe na Bliskim Wschodzie Międzynarodowe Targi Izmirskie .
Siedziba dowództwa południowo-wschodnich kontyngentów NATO .
