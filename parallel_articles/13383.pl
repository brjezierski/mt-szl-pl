Ľudovít Štúr
Ľudovít Štúr , Ludevít Velislav Štúr ( , ur .
28 października 1815 w Uhrovcu , zm .
12 stycznia 1856 w Modrze ) – słowacki pisarz , publicysta , działacz narodowy , językoznawca , kodyfikator języka słowackiego , filozof .
Życiorys .
Jego rodzicami byli Samuel Štúr i jego żona Anna , z domu Michalcová .
W latach 1837–1838 i 1840–1843 Ľudovít Štúr był pedagogiem w ewangelickim liceum w Preszburgu ( Bratysława ) .
W latach 1838–1840 studiował w Halle .
Później prowadził samodzielne badania naukowe nad słowackim językiem , dziejami i kulturą Słowacji .
Od 1851 mieszkał pod nadzorem policji w miejscowości Modra .
Jako nauczyciel bratysławskiego liceum Ľudovít Štúr wykładał filozofię , historię i literaturę słowiańską .
Jego wykłady umacniały w słuchaczach patriotyczny zapał .
