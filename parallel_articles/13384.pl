Łaba ( dopływ Kubania )
Łaba ( ros .
Лаба ) – rzeka w europejskiej części Rosji , na terenie Kraju Krasnodarskiego .
Rzeka powstaje z połączenia rzek Bolszaja Łaba i Małaja Łaba , których źródła znajdują się w Paśmie Głównym Wielkiego Kaukazu .
Rzeka płynie w kierunku północno-zachodnim i uchodzi do rzeki Kubań , naprzeciw miasta Ust´-Łabinsk .
Długość rzeki wynosi 214 km ( 347 km od źródeł Bolszej Łaby ) , a powierzchnia dorzecza – 12 500 km² .
Średni przepływ u ujścia wynosi 95,7 m³/s .
Rzeka jest żeglowna , wykorzystywana do irygacji pól .
Głównymi dopływami są Czamłyk ( prawostronny ) i Fars ( lewostronny ) .
Nad rzeką położone są miasta Łabińsk i Kurganińsk .
