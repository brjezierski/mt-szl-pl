Łambinowice
Łambinowice ( ) – wieś w Polsce , położona w województwie opolskim , w powiecie nyskim , w gminie Łambinowice .
Siedziba gminy wiejskiej Łambinowice .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Łambinowice .
Od 1950 r. wieś położona jest w województwie opolskim .
Nazwa .
Nazwa miejscowości była wzmiankowana po raz pierwszy w 1273 roku w formie `` Lambinouiz '' .
Później notowano także formy `` Lambinovitz '' ( ok. 1300 ) , `` villa Lamberti '' ( 1335 ) , `` Lemlinsdorf '' ( 1371 ) , k `` Glumbwowiczom '' , k. `` Glombinowiczom '' ( 1561 ) , `` Lambsdorff '' ( 1678 ) , `` Lamsdorf '' ( 1784 ) , `` Lammsdorf '' ( 1845 ) , `` Lamsdorf '' , `` Łambinowice '' ( 1941 ) , `` Lamsdorf '' – `` Łambinowice '' , - '' ic '' , `` łambinowicki '' ( 1946 ) .
