Łanczyn ( przystanek kolejowy )
Łanczyn ( , ) – przystanek kolejowy w miejscowości Łanczyn , w rejonie nadwórniańskim , w obwodzie iwanofrankiwskim , na Ukrainie .
Położony jest na linii Kołomyja – Delatyn .
Przed II wojną światową stacja kolejowa .
W późniejszym okresie została zdegradowana do roli przystanku .
