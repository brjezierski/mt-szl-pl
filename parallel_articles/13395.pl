Łask
Łask – miasto w województwie łódzkim , w powiecie łaskim , siedziba gminy miejsko-wiejskiej Łask i powiatu łaskiego .
Jest położone na Wysoczyźnie Łaskiej , nad Grabią .
Według danych GUS z 31 grudnia 2020 r. miasto liczyło 16 925 mieszkańców .
Położenie .
Miasto Łask leży nad Grabią na Wysoczyźnie Łaskiej i w Kotlinie Szczercowskiej .
