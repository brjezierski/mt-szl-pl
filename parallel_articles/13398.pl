Ławica Rønne
Ławica Rønne – ławica na Morzu Bałtyckim , leżąca na duńskich wodach terytorialnych na południowy zachód od wyspy Bornholm .
Wraz z Ławicą Orlą ( z którą jest bezpośrednio połączona ) stanowi jedno z najpłytszych miejsc na południowym Bałtyku .
Na ławicy powstaje farma wiatrowa .
Część ławicy jest objęta obszarem Natura 2000 .
