Arroyos y Esteros
Arroyos y Esteros – miasto w Paragwaju , w departamencie Cordillera .
Miejscowość założył katolicki duchowny Fidel Maíz .
Jako że nie jest znana dokładna data utworzenia miasta , za rocznicę założenia przyjmuje się datę 9 marca 1767 roku , dzień śmierci księdza Maíza .
