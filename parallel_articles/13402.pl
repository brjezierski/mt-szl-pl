Alfabet łaciński
Alfabet łaciński , pismo łacińskie , łacinka , alfabet rzymski – alfabet , system znaków służących do zapisu większości języków europejskich oraz wielu innych .
Jest najbardziej rozpowszechnionym alfabetem na świecie – posługuje się nim około 35 % ludzkości .
W odróżnieniu od greckiego jest de facto abecadłem – ponieważ jego pierwsze litery to : a , b , c , d – a nie alfa i beta .
Wywodzi się z systemu służącego do zapisu łaciny .
