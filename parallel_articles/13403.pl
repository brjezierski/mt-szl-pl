Łańcut
Łańcut – miasto w Polsce położone w województwie podkarpackim , na granicy Podgórza Rzeszowskiego i Pradoliny Podkarpackiej .
Siedziba powiatu łańcuckiego oraz gminy wiejskiej Łańcut .
Leży w dawnej ziemi przemyskiej .
Łańcut należy do województwa podkarpackiego , leżąc 16 km na wschód od jego stolicy – Rzeszowa .
Przez tereny miasta przebiega autostrada A4 , która łączy zachód Europy z Ukrainą .
