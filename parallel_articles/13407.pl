Łeba
Łeba ( , ) – miasto w północnej Polsce , w województwie pomorskim , w powiecie lęborskim , położone na Wybrzeżu Słowińskim , nad rzekami Łebą i Chełstem , miejscowość wypoczynkowa z portem morskim i kąpieliskami .
Według danych z 1 stycznia 2018 Łeba liczyła 3694 mieszkańców .
Historia .
Pierwsze zasiedlenia na brzegu morskim miały miejsce jeszcze w neolicie , są one lokalizowane na pagórkach , np .
koło Potęgowa , nawet na Rowokole archeolodzy znaleźli pojedyncze narzędzia .
Linię brzegu morskiego wskazują do dzisiaj znajdujące się tam grodziska , m.in .
: Gałęzinowo , Żoruchowo , Równo , Siodłonie , Damno .
Siodłonie i Damno były prawdopodobnie oddalone od brzegu morskiego i posadowione nad brzegiem rzeki ; prakoryto tej rzeki jest do dzisiaj czytelne .
Grodziska te to wcześniej grody obronne epoki brązu przypisywane kulturze łużyckiej .
Ludność tej kultury budowała takie grody od ok. 1000 do 400 r. p.n.e .
Naukowcy dokonali rekonstrukcji grodu kultury łużyckiej w Biskupinie .
W okresie , kiedy te grody obronne prowadziły swoje normalne życie , z zatoki tworzyły się jeziora lagunowe .
Przyrastało z czasem trwałego lądu , w okolicy dominowały jednak cały czas jeszcze bagna .
Przeszli tędy Goci .
