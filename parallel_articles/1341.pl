Ars-sur-Formans
Ars-sur-Formans ( wym .
[ ] ) – miejscowość i gmina we Francji w odległości 35 km od Lyonu , w regionie Owernia-Rodan-Alpy , w departamencie Ain .
Miejscowość jest celem licznych pielgrzymek ze względu na osobę świętego Jana Marii Vianneya , który był proboszczem tutejszej parafii przez 41 lat ( od roku 1818 do swojej śmierci w 1859 roku ) i zyskał duży rozgłos .
Nazwa .
Miejscowość w swojej historii nosiła różne nazwy .
Nazywała się kolejno Artis villa , potem Artz następnie Arz , wreszcie Ars .
W niektóre dokumentach możne jeszcze spotkać nazwę Arsa ( spalona ) .
W czasach Jana Marii Vianneya , zwanego Proboszczem z Ars , wieś nosiła nazwę Ars-en-Dembes .
