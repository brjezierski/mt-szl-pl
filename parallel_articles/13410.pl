Wysoczyzna Łobeska
Wysoczyzna Łobeska ( 314.44 ) – mezoregion fizycznogeograficzny w północno-zachodniej Polsce , stanowiący centralną część Pojezierza Zachodniopomorskiego .
Region graniczy od północy z Równiną Gryficką , od zachodu z Równiną Nowogardzką , od południa z Pojezierzem Ińskim , a od wschodu z Pojezierzem Drawskim i Równiną Białogardzką .
Wysoczyzna Łobeska leży w całości w obrębie woj .
zachodniopomorskiego .
Mezoregion jest morenową wysoczyzną ze wzniesieniami sięgającymi ponad 100 m n.p.m .
Na wschód od doliny Regi ( główna rzeka regionu ) , na pograniczu z Pojezierzem Drawskim , leży pasmo znacznie wyższych czołowych moren recesyjnych pod nazwą Gór Kłodzińskich , z najwyższą Klorówką ( 176,2 m ) nad Białym Zdrojem .
W regionie jest mało jezior , a te istniejące są niewielkie .
Wysoczyznę Łobeską przecinają głębokie doliny rzek ( pierwotnie rynny subglacjalne ) , głównie Regi i jej dopływów ( Ukleja , Brzeźnicka Węgorza , Stara Rega , Łoźnica ) .
W północno-zachodniej części Rezerwat przyrody Mszar nad jeziorem Piaski .
Gospodarka regionalna ma mieszany charakter rolniczo-leśniczy .
Głównymi ośrodkami miejskimi regionu są Łobez , Świdwin i Dobra Nowogardzka , ponadto wsie Rąbino , Radowo Małe i Brzeżno .
Głównymi ciągami komunikacyjnymi są : Linia kolejowa nr 202 , DW146 , DW147 , DW148 , DW151 , DW152 i DW162 .
