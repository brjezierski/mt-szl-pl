Łobżany
Łobżany ( niem .
`` Labes A und D '' , `` Tivoli ( Labes A ) '' ) – wieś sołecka w północno-zachodniej Polsce położona w województwie zachodniopomorskim , w powiecie łobeskim , w gminie Łobez .
Według danych z 29 września 2014 r. wieś miała 75 mieszkańców mieszkających w 12 domach .
We wsi znajduje się siedziba Leśnictwa Łobżany z wieżą obserwacyjną i kamerami służącymi do obserwacji lasów , oraz świetlica wiejska prowadzona przez Łobeski Dom Kultury .
Przez Łobżany wiedzie czarny szlak rowerowy `` Dziedzictwo Techniki i Architektury '' .
Położenie .
Wieś leży około 2,8 km na północ od rogatek Łobza i 76 kilometrów na północny wschód od stolicy województwa – Szczecina .
Łobżany oddalone są około kilometra od drogi wojewódzkiej nr 148 .
Do wsi wiedzie także polna droga dojazdowa , która łączy się z ulicami Rolną i Przemysłową w Łobzie .
Historia .
W okolicy Łobżan znajduje się wpisana do rejestru zabytków ( KI.I.6801/36/71 ) osada , stanowisko kultury łużyckiej i wczesnego średniowiecza .
Łobżany to wieś pofolwarczna , powstała na bazie dwóch folwarków , których zarządcami była rodzina von Borck .
Geneza nazwy wsi wskazuje na to , iż historycznie wieś była ściśle powiązana z Łobzem .
Ostatnim właścicielem połączonych folwarków w 1945 roku była urodzona w Rynowie Martha von Borcke ( 1883-1958 ) .
