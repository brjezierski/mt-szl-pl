Łodziska ( osada leśna )
Łodziska – osada leśna w Polsce położona w województwie mazowieckim , w powiecie ostrołęckim , w gminie Lelis .
Historia .
W latach 1921–1939 wieś leżała w województwie białostockim , w powiecie ostrołęckim , w gminie Dylewo , a od 1931 w gminie Kadzidło .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 155 osób w 28 budynkach mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w Ostrołęce .
Podlegała pod Sąd Grodzki w Ostrołęce i Okręgowy w Łomży ; właściwy urząd pocztowy mieścił się w m. Kadzidło .
W wyniku agresji niemieckiej we wrześniu 1939 wieś znalazła się pod okupacją niemiecką .
Od 1939 do wyzwolenia w 1945 włączona w skład Landkreis Scharfenwiese , rejencji ciechanowskiej Prus Wschodnich III Rzeszy .
W latach 1975–1998 miejscowość administracyjnie należała do województwa ostrołęckiego .
