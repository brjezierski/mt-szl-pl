Łotwa ( obwód miński )
Łotwa ( ; ) – wieś na Białorusi , w obwodzie mińskim , w rejonie miadzielskim , w sielsowiecie Miadzioł .
Historia .
W czasach zaborów wieś w granicach Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Mańkowicze ( od 1927 gmina Hruzdowo ) .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 58 osób , 37 było wyznania rzymskokatolickiego a 21 prawosławnego .
Jednocześnie 33 mieszkańców zadeklarowało polską a 25 białoruską przynależność narodową .
Było tu 12 budynków mieszkalnych .
W 1931 w 16 domach zamieszkiwało 76 osób .
Miejscowość należała do parafii rzymskokatolickiej w m. Miadzioł i prawosławnej w Hruzdowie .
Podlegała pod Sąd Grodzki w Postawach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Mańkowiczach .
Po agresji ZSRR na Polskę w 1939 roku wieś znalazła się w granicach BSRR .
W latach 1941–1944 była pod okupacją niemiecką .
Następnie leżała w BSRR .
Od 1991 roku w Republice Białorusi .
