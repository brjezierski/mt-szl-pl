Łowicz
Łowicz – miasto w Polsce w województwie łódzkim , siedziba powiatu łowickiego .
Demografia .
Według danych Słownika geograficznego Królestwa Polskiego w 1827 roku Łowicz miał 411 domów ( z czego 95 było murowanych ) zamieszkiwanych przez 6693 mieszkańców , w tym 676 żydów .
W 1862 było już 522 domy ( w tym 123 murowane ) zamieszkiwanych przez 5885 mieszkańców , w tym 2061 żydów .
W 1881 roku w Łowiczu mieszkało w sumie 7567 mieszkańców .
Dane z 31.12.2020 roku
Położenie .
Łowicz położony jest na Równinie Łowicko-Błońskiej ( makroregion Nizina Środkowomazowiecka ) i w północno-wschodniej części województwa łódzkiego .
