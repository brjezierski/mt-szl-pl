Łowoszów
Łowoszów ( niem .
`` Lowoschau '' ) – wieś w Polsce położona w województwie opolskim , w powiecie oleskim , w gminie Olesno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa częstochowskiego .
Wioska położona przy drodze wojewódzkiej nr 494 .
Sąsiadujące miejscowości : Olesno ( 5 km ) , Wojciechów ( 3 km ) , Stare Olesno ( 5 km ) , Wędrynia ( 2 km ) , Wachów ( 3 km ) .
W centrum wioski znajduje się kościół rzymskokatolicki pw .
Matki Boskiej Wspomożenia Wiernych .
Nazwa .
Według niemieckiego językoznawcy Heinricha Adamy ’ ego nazwa miejscowości pochodzi od staropolskiej nazwy polowania - `` łowiectwa '' .
W swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu wymienia on jako pierwotną zanotowaną nazwę miejscowości `` Łowosow '' podając jej znaczenie `` Jägerdorf '' - `` Wieś łowczych , myśliwych '' .
W okresie reżimu hitlerowskiego w latach 1936-1945 miejscowość nosiła nazwę `` Lauschen '' .
W odległości ok. 1 km na północ znajduje się źródło rzeki Bogacica .
