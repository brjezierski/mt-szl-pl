Łubiana ( stacja kolejowa )
Łubiana – przystanek kolejowy w Łubianie , w gminie Kościerzyna .
Przez przystanek przebiega linia kolejowa nr 211 .
W roku 2017 przystanek obsługiwał 10–19 pasażerów na dobę .
Infrastruktura .
Przystanek w Łubianie posiada 3 niezadaszone perony .
Od przystanku odchodzi bocznica co zakładów Lubiana i rozlewni gazu .
Budynek dworca .
W 2005 roku dworzec w Łubianie został wyremontowany na koszt Gminy Kościerzyna i mieści się w nim między innymi świetlica oraz sala do aerobiku .
W 2012 w budynku wymieniono ogrzewanie , a w 2014 dokonano przebudowy pomieszczeń .
Pociągi .
Przez Łubianę przejeżdżają 4 pary pociągów osobowych pomiędzy Kościerzyną a Chojnicami
Pociągi te są obsługiwane przez szynobusy oraz obecnie zastępczą komunikacje autobusową .
