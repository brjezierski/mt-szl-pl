Arsenal F.C .
Arsenal Football Club ( wym .
lub ; przydomek Kanonierzy ) – angielski klub piłkarski z siedzibą w północnym Londynie , w dzielnicy Holloway .
Jest jedną z najbardziej utytułowanych angielskich drużyn , 13-krotnym mistrzem kraju , 14-krotnym zdobywcą Pucharu Anglii i najdłużej utrzymującym się w najwyższej klasie rozgrywkowej zespołem w Anglii – od 1919 roku .
Arsenal został założony w 1886 roku i był pierwszym klubem z południa Anglii , który dołączył do The Football League , w 1893 roku .
W latach 30 .
XX w. klub pięć razy był mistrzem kraju i zdobył dwa Puchary Anglii .
Po II wojnie światowej klub osiągał mniej sukcesów .
W sezonie 1970/1971 Arsenal zdobył dublet .
Od lat 90. sześciokrotnie wygrywał rozgrywki Pucharu Anglii , oraz czterokrotnie był mistrzem Anglii .
W sezonie 2003/2004 Arsenal zdobył tytuł mistrza Anglii bez żadnej porażki i został drugą drużyną , która to uczyniła ( a pierwszą , która nie przegrała w 38 meczach ) .
Dwa lata później dotarł do finału Ligi Mistrzów , w którym przegrał z FC Barceloną .
Tradycyjnymi barwami klubu są czerwony i biały .
Barwy te w historii klubu przyjmowały różne odcienie .
Arsenal zmieniał także swój stadion ; początkowo mieścił się on w Woolwich , w południowo-wschodnim Londynie , w 1913 roku drużyna przeniosła się na północ miasta , do Arsenal Stadium w Highbury .
W 2006 roku klub przeprowadził się na Emirates Stadium położony w Holloway .
Klub posiada liczną i wierną grupę kibiców .
Największym rywalem zespołu jest Tottenham Hotspur , a mecze Arsenalu z tym klubem określane są jako derby północnego Londynu .
