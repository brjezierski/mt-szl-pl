Ługańsk
Ługańsk ( ukr .
Луганськ , `` Łuhanśk '' ; ros .
Луга́нск , `` Ługansk '' ) – miasto we wschodniej części Ukrainy , przy granicy z Rosją .
Stolica obwodu ługańskiego .
Leży w Donieckim Zagłębiu Węglowym .
W latach 1935–1958 i 1970–1991 nosił nazwę Woroszyłowgrad .
Historia .
14 listopada 1795 r. caryca Katarzyna II wydała ukaz nakazujący budowę pierwszej huty żelaza na południu imperium .
Zadanie to zostało powierzone szkockiemu architektowi i mechanikowi Charlesowi Gascoigne .
Pierwszy wielki piec uruchomiono 4 października 1800 r. W 1812 roku , podczas wojny z Napoleonem , Zakłady Ługańskie były ważnym ośrodkiem produkcji armat i amunicji .
Wokół zakładów metalurgicznych zaczęła rozwijać się osada rzemieślniczo-handlowa .
Rozwinięty był zwłaszcza drobny przemysł tłuszczowy .
Duży impuls dla intensywnego rozwoju przemysłu dało otwarcie linii kolejowej do Debalcewa w 1878 roku ( Kolej Jekateryninska ) .
3 września 1882 r. imperator Aleksander III zatwierdził postanowienie rosyjskiej rady ministrów o utworzeniu miasta Ługańsk na bazie osady wokół zakładów oraz wsi Kamjanyj Brid ( ros .
Kamiennyj Brod ) .
W 1896 r. niemiecki przedsiębiorca Gustaw Hartmann otworzył w Ługańsku fabrykę parowozów , a Belgowie otworzyli fabrykę wyrobów emaliowanych .
W 1897 miasto liczyło 20,4 tysiące mieszkańców , z tego około połowa pracowała w przemyśle .
Po rewolucji październikowej miasto weszło w skład Doniecko-Krzyworoskiej Republiki Radzieckiej , której na krótko zostało stolicą w kwietniu 1918 r. Ciężkie walki o Ługańsk toczyły się podczas walk o Donbas między marcem a majem 1919 r. , opanowane przez czerwonych w marcu 1919 r. miasto zostało zdobyte przez białych 4 maja 1919 r. po zaciętej walce .
Czerwoni odzyskali Ługańsk w toku operacji donbaskiej 27 grudnia tego samego roku .
Pod rządami radzieckimi nastąpił dalszy rozwój przemysłu , zwłaszcza w postaci znacznie rozbudowanej fabryki parowozów , noszącej teraz nazwę zakładów im .
Rewolucji Październikowej , uruchomionej ponownie w 1933 .
W 1926 ludność liczyła 71,8 tysiąca , a w 1934 – 196 tysięcy .
W 1931 roku w Ługańsku pojawiły się autobusy miejskie , a w 1934 roku uruchomiono linię tramwajową .
Od 1938 roku miasto zostało stolicą obwodu ługańskiego .
Pomiędzy 17 lipca 1942 a 14 lutego 1943 Ługańsk znajdował się pod okupacją niemiecką .
Po 1945 roku w mieście istniały dwa obozy dla jeńców niemieckich .
W latach 1935–1958 i 1970–1991 miasto nazywało się Woroszyłowgrad , na cześć Klimienta Woroszyłowa , któremu wzniesiono również w mieście konny pomnik .
6 kwietnia 2014 Ługańsk został faktycznie opanowany przez prorosyjskich separatystów , będących przeciwnikami rządu w Kijowie .
Stanowił stolicę istniejącej do 2022 roku nieuznawanej międzynarodowo Ługańskiej Republiki Ludowej .
W mieście jest kilka uczelni , między innymi Państwowy Uniwersytet Rolniczy , Uniwersytet Pedagogiczny , Uniwersytet Medyczny .
Ludność .
Największą liczbę ludności Ługańsk miał w 1995 r. – 535 800 osób .
Według spisu powszechnego z roku 2001 Ługańsk liczył 463 tys .
osób .
Narodowość ukraińską deklarowało 50 % mieszkańców , a rosyjską – 47 % .
Dla 84,73 % ludności językiem ojczystym był rosyjski , a dla 14,33 % ukraiński .
W 2013 roku w Ługańsku mieszkało 425 848 osób .
