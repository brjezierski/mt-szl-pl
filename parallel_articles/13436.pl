Łukasz Garlicki
Łukasz Garlicki ( ur .
5 sierpnia 1977 w Warszawie ) – polski aktor teatralny , filmowy i telewizyjny , reżyser teatralny .
Życiorys .
W 2000 ukończył studia w Państwowej Wyższej Szkole Teatralnej w Warszawie .
Jeden z założycieli Projektu Warszawiak .
W 2011 był prowadzącym gali rozdania Polskich Nagród Filmowych ( „ Orłów ” ) .
Wiosną 2015 brał udział w trzeciej edycji programu rozrywkowego Polsatu `` Dancing with the Stars .
Taniec z gwiazdami '' .
W parze z Magdaleną Soszyńską-Michno odpadł w piątym odcinku , zajmując 8. miejsce .
Życie prywatne .
Jest synem Moniki i Piotra Garlickich .
