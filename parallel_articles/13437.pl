Łukasz Kohut
Łukasz Marcin Kohut ( ur .
10 września 1982 w Katowicach ) – politolog , przedsiębiorca , działacz społeczny i polityk ; lider Wiosny w województwie śląskim , koordynator regionalny partii w trzech województwach ( śląskie , opolskie i małopolskie ) ; członek Demokratycznej Unii Regionalistów Śląskich i stowarzyszenia Śląskie Perły ; poseł do Parlamentu Europejskiego IX kadencji ; fotografik .
Życiorys .
Wykształcenie .
W 1997 r. ukończył Szkołę Podstawową nr 4 w Radlinie .
Absolwent politologii na Wydziale Nauk Społecznych Uniwersytetu Śląskiego ( 2006 ) oraz zarządzenia i marketingu Szkoły Zarządzania UŚ ( 2007 ) .
W 2006 studiował w ramach programu Socrates Erasmus zarządzanie i marketing w fińskim Varkaus na Savonia University of Applied Sciences .
Ukończył także kurs pedagogiczny na Politechnice Krakowskiej ( 2005 ) oraz Szkołę Liderów Społeczeństwa Obywatelskiego ( 2014 ) .
Oprócz języka polskiego , deklaruje znajomość języków - angielskiego , norweskiego , czeskiego i śląskiego .
Kariera zawodowa .
Tuż po studiach pracował w banku w Sosnowcu .
W 2008 r. wyemigrował do Norwegii , gdzie pracował m.in .
w księgowości .
W 2010 przeniósł się do czeskiej Pragi , gdzie koordynował projekt międzynarodowy między Norwegią , Czechami i Filipinami .
Koordynował także wymianę międzynarodową pomiędzy Ås videregående skole a ZST Rybnik .
Od 2015 do 2019 prowadził własną działalność gospodarczą jako fotograf .
Działalność społeczno-polityczna .
Po powrocie do Polski w 2012 został członkiem Ruchu Palikota , który w 2013 przekształcił się w Twój Ruch .
W 2012 został sekretarzem struktur RP w Rybniku , a w 2014 przewodniczącym zarządu partii w okręgu nr 30 .
W wyborach samorządowych w tym samym roku startował z listy Twojego Ruchu do sejmiku województwa śląskiego , otrzymując 1435 głosów .
W wyborach parlamentarnych w 2015 startował jako przedstawiciel TR z listy Zjednoczonej Lewicy z drugiego miejsca , uzyskując 5397 głosów ( najlepszy wynik na liście ) .
W 2015 został także członkiem Demokratycznej Unii Regionalistów Śląskich – stowarzyszenia , które promuje edukację regionalną i zarejestrowanie języka śląskiego jako języka regionalnego .
W 2016 wraz ze stowarzyszeniem Śląskie Perły , którego został członkiem , współorganizował czarne protesty w subregionie południowym .
Stowarzyszenie to zainicjowało także w 2018 powstanie rybnickiej rady kobiet .
Zwyciężył w plebiscycie „ Dziennika Zachodniego ” Człowiek Roku 2016 w Rybniku .
W październiku 2017 jako członek Twojego Ruchu uczestniczył m.in .
w proteście przeciwko prezydentowi RP Andrzejowi Dudzie podczas jego wizyty w Wodzisławiu Śląskim .
W 2018 wystąpił z TR .
W wyborach samorządowych w tym samym roku miał startować do sejmiku wojewódzkiego jako lider okręgowej listy Koalicji Obywatelskiej , jednak ostatecznie Nowoczesna rekomendowała na to miejsce Wojciecha Kałużę , w związku z czym nie wystartował .
Od 2017 współpracował z Robertem Biedroniem w ramach Instytutu Myśli Demokratycznej .
W 2019 został członkiem partii Wiosna oraz kandydatem nr 1 Wiosny w wyborach do Parlamentu Europejskiego w okręgu wyborczym nr 11 ( województwo śląskie ) .
Uzyskał 48 783 głosy ( 3,05 % ) , zdobywając mandat europosła IX kadencji jako jeden z trzech polityków Wiosny .
W 2021 , w związku z połączeniem Wiosny z SLD , został deputowanym Nowej Lewicy .
Fotografia .
Specjalizuje się w śląskim krajobrazie i fotografii koncertowej .
W 2016 w rybnickiej galerii Smolna zadebiutował wystawą `` Śląska melancholia '' .
W tym samym roku powstała druga wystawa o Śląsku – `` FEST fotografia '' prezentowana m.in .
na rybnickim deptaku i podczas Rybnickiego Festiwalu Fotografii .
W 2017 w „ Halo !
Rybnik ” zaprezentował `` Pogranicze '' – trzecią część opowieści o Śląsku .
W 2018 w ramach Śląskiej Fotografii Prasowej zdjęcie `` Dobrze już było '' otrzymało główną nagrodę czytelników Biblioteki Śląskiej , a zdjęcie `` Jastrzębie i zdrój '' nagrodę specjalną Galerii Sztuki Współczesnej BWA w Katowicach .
