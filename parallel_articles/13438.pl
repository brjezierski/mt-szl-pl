Łukasz Simlat
Łukasz Simlat ( ur .
11 grudnia 1977 w Sosnowcu ) – polski aktor .
Życiorys .
Absolwent II Liceum Ogólnokształcącego im .
Emilii Plater w Sosnowcu .
Przez dwa lata uczęszczał do studia aktorskiego „ Art-Play ” Doroty Pomykały i Danuty Owczarek w Katowicach .
W 2000 ukończył studia w Akademii Teatralnej w Warszawie ( bez dyplomu ) .
Po ukończeniu studiów występował gościnnie w kilku teatrach bez stałego etatu .
Od 3 grudnia 2007 jest etatowym aktorem warszawskiego Teatru Powszechnego .
W 2012 dzięki Agnieszce Glińskiej został członkiem zespołu artystycznego Teatru Studio w Warszawie .
Jego teatralne dokonania artystyczne zostały wielokrotnie docenione i nagrodzone .
Krytycy podkreślają bogactwo środków artystycznych , jakie aktor ten potrafi wydobyć i zastosować w kreowaniu różnych – często zupełnie kontrastowych – postaci .
Jest żonaty .
Role filmowe .
W filmie zadebiutował rolą epizodyczną w serialu `` Dom '' ( 2000 ) .
Przez jakiś czas grał drobne role w filmach ( m.in .
`` Warszawa '' ) i serialach .
Na planie II części `` Bożej podszewki '' dostrzegła go reżyser Izabella Cywińska , mimo iż spędził tam zaledwie jeden dzień zdjęciowy , i wkrótce powierzyła mu ambitną rolę rozdartego wewnętrznie Arka w produkcji `` Kochankowie z Marony '' ( 2005 ) .
Później Simlat miał też okazję zagrać Szymona w uhonorowanym kilkoma nagrodami filmie `` Statyści '' Michała Kwiecińskiego ( 2006 ) oraz drugoplanową , ale dającą się zapamiętać , rolę ojca Adasia we `` Wszyscy jesteśmy Chrystusami '' Marka Koterskiego .
Grał rolę księgowego Adama Turka w serialu `` BrzydUla '' , emitowanym w stacji TVN .
