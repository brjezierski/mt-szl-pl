Łukasz Surma
Łukasz Dawid Surma ( ur .
28 czerwca 1977 w Krakowie ) – polski piłkarz i trener piłkarski .
Wychowanek Wisły Kraków , pięciokrotny reprezentant Polski .
Rekordzista w liczbie występów w Ekstraklasie , co czyni go liderem tzw .
klubu 300 .
Jest pierwszym zawodnikiem w historii polskiej Ekstraklasy , który rozegrał przynajmniej po sto meczów ligowych w trzech różnych klubach .
W latach 2018–2019 trener Soły Oświęcim , w latach 2019–2021 Garbarni Kraków , w latach 2021–2022 Piasta Żmigród , w latach 2022–2023 Stali Stalowa Wola .
Kariera piłkarska .
W Ekstraklasie zadebiutował 27 lipca 1996 w barwach Wisły Kraków w spotkaniu 1. kolejki sezonu 1996/1997 z GKS Bełchatów , w wieku 19 lat i 29 dni .
Przed sezonem 1998/1999 trafił do Ruchu Chorzów .
W klubie tym przez cztery sezony zagrał 117 meczów , strzelił 6 bramek oraz w sezonie 1999/2000 zdobył brązowy medal mistrzostw Polski .
W Legii Warszawa rozegrał 123 mecze ligowe , strzelając w nich 8 bramek .
W warszawskim zespole występował na pozycji defensywnego pomocnika .
W ciągu pięciu lat pełnił funkcję kapitana tej drużyny .
W sezonie 2005/2006 wywalczył mistrzostwo Polski .
6 lipca 2007 podpisał dwuletni kontrakt z izraelskim klubem Maccabi Hajfa , gdzie po rozegraniu jednego meczu i zdobyciu Toto Cup przeniósł się do Bene Sachnin .
W nowym zespole był podstawowym zawodnikiem ; jego drużyna zajęła 3. miejsce w lidze , dające prawo gry w Pucharze UEFA .
