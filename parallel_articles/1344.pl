Arthrobotrys oligosporus
Arthrobotrys oligosporus Fresen .
– gatunek workowców .
Grzyb drapieżny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Orbiliaceae , Orbiliales , Orbiliomycetidae , Orbiliomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1832 r. Lewis Schweinitz nadając mu nazwę `` Dothidea pomigena '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu 1920 r. Arthur Samuel Colby .
Synonimy :
Morfologia i tryb życia .
`` Arthrobotrys oligosporus '' występuje w glebie .
Tworzy ze swoich strzępek specjalną sieć łowną tworzącą zamknięte pętle .
Gdy penetrujący glebę w poszukiwaniu pożywienia nicień wpełźnie w pierścień , komórki go tworzące w błyskawicznym tempie zaczynają pochłaniać wodę ze środowiska , wskutek czego 3-krotnie zwiększają swoją objętość .
Uduszony nicień ginie , a grzybnia rozrasta się wewnątrz ciała nicienia trawiąc go .
Ze strawionego ciała nicienia wyrastają konidiofory tworzące zarodniki konidialne .
