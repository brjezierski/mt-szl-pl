Łukaszew ( Łódź )
Łukaszew – od 1946 roku osiedle w północno-wschodniej części Łodzi , w dzielnicy Widzew .
Administracyjnie wchodzi w skład osiedla Dolina Łódki .
Jest to niewielkie osiedle o peryferyjnym położeniu .
Rozpościera się w rejonie ulicy Łukaszewskiej , blisko granicy miasta .
