Łukowiec Wiszniowski
Łukowiec Wiszniowski ( ukr .
Луковець-Вишнівський , `` Łukoweć-Wyszniwśkyj '' ) – wieś na Ukrainie , w obwodzie iwanofrankiwskim , w rejonie rohatyńskim .
Około 2,5 km na północ od wsi znajduje się stacja kolejowa Żurawno , położona na linii Lwów – Czerniowce i 11 km na północny wschód od wsi Żurawno .
Prowadzi do niej ul .
Tarasa Szewczenki ( вул .
Тарасa Шевченкa ) .
