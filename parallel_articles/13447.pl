Łupiny ( rejon szczuczyński )
Łupiny ( biał .
`` Лупіны '' , ros .
`` Лупины '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie szczuczyńskim , w sielsowiecie Kamionka .
W czasach zaborów w granicach Imperium Rosyjskiego , w guberni grodzieńskiej , w powiecie grodzieńskim .
W latach 1921–1939 wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim a od 1929 w województwie nowogródzkim , w powiecie szczuczyńskim , w gminie Kamionka .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 86 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Było tu 15 budynków mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w Kamionce .
