Historia .
Pierwsza wzmianka o Łucku pochodzi z 1085 r. W 1073 r. Łuck wszedł w skład księstwa włodzimierskiego .
W 1083 na miasto napadli Litwini i Jaćwingowie .
W XIII w. wzniesiono cerkiew św .
Jana Ewangelisty .
W 1321 r. Łuck zajął Giedymin i osadził w nim swego starostę .
W 1325 r. Lubart wzniósł Zamek Dolny .
Zamek Górny założony został potem przez Olgierda lub Witolda .
W 1366 r. miała miejsce wyprawa Kazimierza III Wielkiego na Lubarta , po czym nadał on prawem lenna ziemie te Aleksandrowi Koriatowiczowi .
Łuck uzyskał prawo składu przed 1379 rokiem .
W 1364 r. król Kazimierz Wielki ufundował biskupstwo wołyńskie z siedzibą biskupa najpierw we Włodzimierzu , a od początku XV w. w Łucku .
Biskupstwo łuckie istniało `` de facto '' od 1375 r. Z czasem kościołem katedralnym został kościół pw .
