Łużyce
Łużyce ( , , , , ) – kraina historyczno-geograficzna o powierzchni około 13 000 km² , usytuowana pomiędzy Kwisą i Łabą , administracyjnie należąca do Niemiec ( część zachodnia ) i Polski ( część wschodnia ) , zamieszkana przez 1,3 mln osób ( Serbołużyczan , Niemców i Polaków ) .
