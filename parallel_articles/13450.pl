Historia .
Ślady osadnictwa na terenie Łużyc pozostawiły plemiona celtyckie .
Zastąpiły je plemiona wschodniogermańskie , a tych miejsce , w efekcie wędrówki ludów , zajęły plemiona słowiańskie określane mianem Słowian połabskich .
Potomkami niektórych z nich są dzisiejsi Serbołużyczanie .
W latach 623–658 Łużyce wchodziły w skład państwa Samona – najstarszego znanego współcześnie tworu państwowego Słowian zachodnich .
Od VII do VIII wieku na terenie Łużyc istniały państwa plemienne zarządzane przez rodzimych władców , jeden z nich Miliduch , król plemion serbo-łużyckich , w 806 roku stoczył bitwę z wojskami Franków .
W IX wieku wchodziły w skład Państwa Wielkomorawskiego .
Stan ten utrzymywał się do 907 roku .
W roku 963 zostały podbite przez Gerona , margrabiego saskiej Marchii Wschodniej .
W roku 1002 Bolesław I Chrobry przyłączył Łużyce do Polski , co zostało uznane w pokoju w Budziszynie ( 1018 ) .
Następnie ponownie znalazły się w obrębie wpływów niemieckich ( układ pokojowy z Budziszyna , 1031 r. ) jako część Marchii Miśnieńskiej .
Od XII wieku intensywne osadnictwo niemieckie – przede wszystkim w górach , w miastach i niezasiedlonych obszarach – spowodowało , że Słowianie stali się od XIII wieku mniejszością w tym regionie .
W okresie od 1076 do 1253 r. Łużyce Górne należały do Czech , po czym cały region przypadł Saksonii .
W czasach królów Jana i Karola IV z dynastii luksemburskiej trwale zintegrowane z Koroną Królestwa Czech ( Górne w 1319 r. i 1329 r. , Dolne w 1368 r. ) .
Książęta piastowscy z linii świdnickiej obejmowali na krótko rządy na Łużycach Górnych ( Henryk I jaworski w latach 1319–1346 ) i Dolnych ( Bolko II świdnicki w latach 1364–1368 ) .
W 1415 r. doszło do utraty przez Luksemburgów panowania nad Brandenburgią i częścią Łużyc Dolnych z Chociebużem .
W ramach Korony Czeskiej reszta Łużyc pozostawała do 1635 r. , kiedy przeszła pod panowanie Saksonii w ramach pokoju praskiego .
