Łęczna ( Nowy Dwór Mazowiecki )
Łęczna – część miasta Nowego Dworu Mazowieckiego , w województwie mazowieckim , w powiecie nowodworskim .
Leży w środkowo-południowej części Nowego Dworu , w pobliżu Wisły .
Do 1952 samodzielna miejscowość .
W latach 1867–1952 wieś w gminie Góra w powiecie warszawskim ; 20 października 1933 utworzyła gromadę w granicach gminy Góra , składającą się z samej wsi Łęczna i wsi Nowo-Łęczna .
W związku z utworzeniem powiatu nowodworskiego 1 lipca 1952 , gromadę Łęczna wyłączono z gminy Góra i włączono do gminy Janówek .
