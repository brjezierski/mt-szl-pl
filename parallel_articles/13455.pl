Łęczyca ( Warszawa )
Łęczyca – południowo-wschodnia część warszawskiej dzielnicy Ursynów .
Według Miejskiego Systemu Informacji Ursynów stanowi część obszaru Skarpa Powsińska .
Graniczy z Konstancinem-Jeziorną .
Według państwowego rejestru nazw geograficznych osiedle to część miasta o identyfikatorze 173692 i SIMC 0918376 .
Łęczyca zajmuje obszar wzdłuż ulicy Prawdziwka .
Historia .
W latach 1867–1954 wieś w gminie Jeziorna w powiecie warszawskim .
20 października 1933 utworzyła gromadę Łęczyca w granicach gminy Jeziorna .
Następnie włączona do gromady Gawroniec-Janówek .
15 maja 1951 gromadę Gawroniec-Janówek podzielono , włączając Łęczycę do Warszawy .
