Łyśwa
Łyśwa ( ) – miasto w Rosji , w Kraju Permskim .
Leży na Uralu , nad rzeką Łyśwą ( dorzecze Kamy ) .
Została założona w 1785 , prawa miejskie uzyskała w 1926 .
Jest ośrodkiem hutnictwa żelaza i przemysłu metalowego oraz przemysłu maszynowego ( turbogeneratory ) , materiałów budowlanych , odzieżowego i dziewiarskiego .
