Łąka Prudnicka
Łąka Prudnicka ( , , ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na pograniczu Gór Opawskich i Obniżenia Prudnickiego , będącego częścią Niziny Śląskiej .
Przepływa przez nią rzeka Złoty Potok i potok Zamecki Potok .
W latach 1975–1998 wieś administracyjnie należała do ówczesnego woj .
