Łańce
Łańce ( niem .
`` Lohnitz '' ) – wieś w Polsce położona w województwie śląskim , w powiecie raciborskim , w gminie Kornowac .
W latach 1975–1998 miejscowość administracyjnie należała do województwa katowickiego .
Geografia .
Miejscowość jest położona na terenie wyniesionym około 100 m ponad dolinę Odry , przez co ma charakter przepięknego , malowniczego punktu widokowego .
Historia .
Nazwa miejscowości wywodzi się od łanu – miary obszaru ziemi nadawanej osadnikowi , który płacił czynsz .
Wioska wzmiankowana w 1454 i 1479 roku jako `` Lanczuow '' .
W końcu XVII wieku właścicielem wsi został Jan Fryderyk Kotuliński , który zapisał klasztorowi ojców Franciszkanów znaczne fundacje i zobowiązał swoich potomków oraz przyszłych właścicieli do rocznych danin na rzecz wspólnoty zakonnej .
Zabytki .
Kapliczka .
Kapliczka – domkowa , zbudowana na początku XIX wieku na planie prostokąta z dwuspadowym dachem , z wieżyczką nakrytą stożkowatym dachem , który został zwieńczonym krzyżykiem .
