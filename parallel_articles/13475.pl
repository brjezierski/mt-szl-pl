Województwo łódzkie
Województwo łódzkie – jedno z 16 województw Polski , położone w centralnej części kraju .
Obejmuje obszar o powierzchni 18 218,95 km² .
Według danych z 30 czerwca 2020 roku województwo zamieszkiwało około 2,4 mln osób .
Ma najmniejszą lesistość w kraju .
Siedzibą władz województwa jest Łódź .
Historia .
Utworzone w 1999 roku z województw poprzedniego podziału administracyjnego :
Administracyjne i gospodarcze kształtowanie się województw łęczyckiego , sieradzkiego , rawskiego i łódzkiego w ciągu całej historii państwowości polskiej zostało szczegółowo przedstawione w monografii prof. Marka Kotera .
Geografia .
