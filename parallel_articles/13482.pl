Łomża
Łomża – miasto na prawach powiatu w północno-wschodniej Polsce , w województwie podlaskim , nad rzeką Narew .
Miasto znajduje się na historycznym Mazowszu , było stolicą dawnej ziemi łomżyńskiej .
Łomża to główny ośrodek gospodarczy , edukacyjny i kulturowy ziemi łomżyńskiej .
Miasto królewskie Korony Królestwa Polskiego w województwie mazowieckim .
Miejsce obrad sejmików ziemskich ziemi łomżyńskiej od XVI wieku do pierwszej połowy XVIII wieku .
W latach 1975–1998 była stolicą województwa łomżyńskiego .
Od 1 stycznia 1999 roku jest siedzibą powiatu łomżyńskiego .
Od 28 października 1925 jest także stolicą diecezji łomżyńskiej Kościoła rzymskokatolickiego .
Według danych GUS z 31 grudnia 2021 r. , Łomża liczyła 60 468 mieszkańców i była pod względem liczby ludności trzecim ( po Białymstoku oraz Suwałkach ) miastem w województwie podlaskim , a także 65. spośród najludniejszych miast w Polsce .
Położenie .
Łomża jest położona w środkowym biegu Narwi na Nizinie Mazowieckiej na wysokości ok. 125 m n.p.m .
w północnej części Międzyrzecza Łomżyńskiego .
Miasto leży na wzgórzach morenowych , obejmujących wysoką , lewobrzeżną skarpę pradoliny Narwi .
