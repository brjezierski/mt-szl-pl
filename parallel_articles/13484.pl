Łączka ( wydma )
Łączka ( pot .
t. Łącka Góra , również Łącka Wydma ) – wzniesienie wydmowe o zmiennej wysokości około 30 m n.p.m .
na Mierzei Łebskiej , na Wybrzeżu Słowińskim , położone w woj .
pomorskim , w powiecie słupskim , w gminie Smołdzino .
Łączka znajduje się w Słowińskim Parku Narodowym .
Do 1945 r. stosowano niemiecką nazwę `` Lontzken Berg '' .
W 1955 r. wprowadzono urzędowo polską nazwę `` Łączka '' .
