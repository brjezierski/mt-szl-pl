Ń
Ń – dziewiętnasta litera alfabetu polskiego , służąca do zapisu spółgłoski nosowej dziąsłowo-podniebiennej .
Jest także używana w języku kazachskim , języku dolnołużyckim , etnolekcie wilamowskim ( wymysiöeryś ) oraz łacince białoruskiej ( odpowiednik cyrylicznego : нь ) .
W językach czeskim i słowackim jej odpowiednikiem jest ň , hiszpańskim ñ , portugalskim nh , a w serbsko-chorwackim nj / њ .
W języku polskim „ ń ” nie jest pierwszą literą żadnego wyrazu .
