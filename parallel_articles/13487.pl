Niagań
Niagań ( ) – miasto w Rosji , w Chanty-Mansyjskim Okręgu Autonomicznym - Jugrze .
Ośrodek przemysłu naftowego i drzewnego .
Historia .
W 1954 r. niedaleko dzisiejszego miasta założono osadę drwali Niagań ( obecnie nosi ona nazwę Stary Niagań ) .
W 1965 r. w jej pobliżu założono osadę Niach , która stała się zaczątkiem obecnego miasta .
Szybki rozwój miasta nastąpił w latach 70. w związku z odkryciem i eksploatacją złóż ropy naftowej i gazu ziemnego .
Obecnie przemysł naftowy stanowi główną gałąź gospodarki miasta .
W 1985 r. Niagań otrzymał prawa miejskie .
W 1987 r. urodziła się tutaj tenisistka Marija Szarapowa .
