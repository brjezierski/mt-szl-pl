Niger
Niger , Republika Nigru ( fr .
`` République du Niger '' ) – państwo położone w Afryce Zachodniej na Saharze , bez dostępu do morza .
Niger liczy ponad 1,3 mln km² powierzchni .
Graniczy z Algierią ( 956 km ) , Libią ( 354 km ) , Czadem ( 1175 km ) , Nigerią ( 1497 km ) , Beninem ( 266 km ) , Burkina Faso ( 628 km ) i z Mali ( 821 km ) .
W 2011 roku w państwie mieszkało 15 306 252 osób .
Kraj uzyskał niepodległość 3 sierpnia 1960 roku .
Językiem urzędowym jest francuski .
Niger dzieli się na 7 departamentów i jeden dystrykt stołeczny .
Stolicą jest Niamey .
Niger należy do Międzynarodowej Agencji Energii Atomowej , Międzynarodowej Organizacji Pracy , Międzynarodowego Banku Odbudowy i Rozwoju , Międzynarodowego Funduszu Walutowego , Międzynarodowego Trybunału Karnego , Organizacji Konferencji Islamskiej , Organizacji Narodów Zjednoczonych , Unii Afrykańskiej i Światowej Organizacji Handlu i ECOWAS .
Geografia .
Prawie cały obszar Nigru leży w południowej Saharze .
Niger znajduje się na powierzchni wyżynnej ( wys .
200–500 m ) .
W centrum państwa wznosi się Wyżyna Air , zbudowana ze skał krystalicznych .
Na wyżynie znajdują się wulkaniczne masywy ( m.in .
Baguezane , 2022 m ) .
Na wschód od wyżyny rozciąga się pustynna niecka Tenere , a na zachód niecka Talak .
Na północy Nigru znajduje się płaskowyż Dżado osiągający do 1150 metrów wysokości .
Klimat zwrotnikowy kontynentalny , suchy i skrajnie suchy na północy .
W części środkowej i południowej klimat jest podrównikowy suchy ( wilgotniejszy na skrajnym południu ) .
Średnia roczna suma opadów waha się pomiędzy 20–50 mm na północy do 600–800 mm na południu .
Pora deszczowa przypada w sierpniu w części środkowej .
W części południowej pora deszczowa rozpoczyna się w czerwcu , a kończy we wrześniu .
Średnia temperatura w styczniu ( będącym najchłodniejszym miesiącem ) wynosi 16–17 °C na północy i 24 °C na południu .
W części północnej najcieplejszym porą jest lipiec zaś w części środkowej i południowej okres pomiędzy kwietniem i majem .
