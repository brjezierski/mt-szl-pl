Nigeria
Nigeria , Federalna Republika Nigerii ( ) – państwo w Afryce Zachodniej nad Zatoką Gwinejską , najludniejsze na kontynencie afrykańskim .
Nazwa kraju pochodzi od rzeki Niger .
Po raz pierwszy pojawiła się w brytyjskiej gazecie The Times w 1897 roku .
Państwo sąsiaduje z Beninem , Nigrem , Czadem i Kamerunem .
Obecnie członek Unii Afrykańskiej i ECOWAS .
Ustrój polityczny .
Nigeria jest republiką federalną .
Głową państwa jest prezydent Muhammadu Buhari , który jednocześnie sprawuje funkcję szefa rządu .
Siły zbrojne .
Nigeria dysponuje trzema rodzajami sił zbrojnych : wojskami lądowymi , marynarką wojenną oraz siłami powietrznymi .
Uzbrojenie sił lądowych Nigerii składało się w 2014 roku z : 363 czołgów , 1450 opancerzonych pojazdów bojowych , 29 dział samobieżnych , 21 wieloprowadnicowych wyrzutni rakietowych oraz 680 zestawów artylerii holowanej .
Marynarka wojenna Nigerii dysponowała w 2014 roku 51 okrętami obrony przybrzeża , jedną fregatą oraz dwoma okrętami obrony przeciwminowej .
Nigeryjskie siły powietrzne natomiast posiadały uzbrojenie w postaci 10 myśliwców , 36 samolotów transportowych , 40 samolotów szkolno-bojowych , 36 śmigłowców oraz 8 śmigłowców szturmowych .
Wojska nigeryjskie w 2014 roku liczyły 130 tys .
