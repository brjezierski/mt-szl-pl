Nikaragua
Nikaragua , Republika Nikaragui ( hiszp .
`` Nicaragua '' , `` República de Nicaragua '' ) – państwo w Ameryce Środkowej , położone między Hondurasem na północy a Kostaryką na południu .
Na zachodzie na długości 320 km oblewają ją wody Oceanu Spokojnego , a na wschodzie na długości 480 km – wody Morza Karaibskiego .
Geografia .
Wschodnie wybrzeże Nikaragui ma charakter wybrzeża lagunowego .
Nadmorska nizina , miejscami zabagniona , nosi nazwę Wybrzeża Moskitów .
Dalej ku zachodowi ciągnie się wyżyna , którą przecina kilka pasm górskich .
Najwyższe z nich , Cordillera Isabelia ze szczytem Mogotón , osiąga wysokość 2438 m n.p.m .
Krawędź tej wyżyny , równoległa do wybrzeża pacyficznego , stanowi północno-wschodnią ścianę rowu tektonicznego .
W jego zagłębieniu znajdują się dwa wielkie jeziora – Nikaragua i Managua .
Za rowem tektonicznym , wzdłuż wybrzeża , ciągnie się rząd 40 wulkanów .
