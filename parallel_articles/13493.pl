Nikiszowiec
Nikiszowiec ( niem .
`` Nickischschacht '' , śl .
`` Ńikisz '' ) – zabytkowe osiedle w Katowicach w jednostce pomocniczej Janów-Nikiszowiec , powstałe w latach 1908–1919 z inicjatywy koncernu górniczo-hutniczego `` Georg von Giesches Erben '' jako osiedle robotnicze dla górników kopalni `` Giesche '' ( obecnie `` Wieczorek '' ) wybudowane na terenie obszaru dworskiego Giszowiec .
Osiedle zaprojektowali architekci Emil i Georg Zillmannowie z Charlottenburga , którzy byli również autorami projektu sąsiedniego Giszowca .
W dniu 9 maja 1924 roku nastąpiła likwidacja obszaru dworskiego i Nikiszowiec wraz z Giszowcem włączono do gminy Janów .
W 1951 roku osiedle stało się częścią nowego miasta – Szopienic .
31 grudnia 1959 roku Nikiszowiec wraz z Szopienicami i Janowem został włączony do Katowic .
Osiedle to , podobnie jak Giszowiec , znajduje się na Szlaku Zabytków Techniki Województwa Śląskiego .
