Nikobary
Nikobary – złożony z 22 wysp , wydłużony południkowo archipelag , należący do Indii .
Znajduje się on na Oceanie Indyjskim , między Zatoką Bengalską i Morzem Andamańskim .
Od Andamanów oddziela je Kanał Dziesiątego Stopnia .
Razem z nimi tworzy terytorium związkowe Andamany i Nikobary .
Geografia .
Łączna powierzchnia archipelagu wynosi 1841 km² , a liczba ludności w 2001 – 42 068 os. , w tym ok. 26 tysięcy rdzennych Nikobarczyków .
Główne wyspy :
Na Nikobarach panuje klimat równikowy , wybitnie wilgotny .
Uprawy kokosów , trzciny cukrowej , owoców cytrusowych , ananasów i ryżu .
Nikobary wraz z położonymi dalej na północ Andamanami wchodzą w skład podwodnego łańcucha górskiego , wypiętrzonego w czasie orogenezy fałdowania alpejskiego , będącego częścią pasma fałdowego Birmy .
Historia .
Nikobarczycy są potomkami podróżników z Półwyspu Malajskiego , którzy przybyli na wyspy kilka tysięcy lat temu .
W 1756 roku archipelag został zajęty przez Duńczyków .
Następnie przez habsburską Austrię , a potem przez Brytyjczyków .
W czasie II wojny światowej zostały zajęte przez Japończyków i odbite przez Brytyjczyków .
W 1947 roku przeszły na własność niepodległych Indii .
Od 1956 roku Indie ograniczyły prawnie dostęp do wyspy w celu ochrony ich unikalnego charakteru .
