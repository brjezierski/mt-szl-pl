Niżny Nowogród
Niżny Nowogród ( , `` Niżnyj Nowgorod '' ; w latach 1932–1990 Gorki ) – miasto wydzielone w Rosji , stolica Nadwołżańskiego Okręgu Federalnego i obwodu niżnonowogrodzkiego , położone nad Wołgą , u ujścia Oki ; wielki ośrodek przemysłowy w regionie Wołżańsko-Wiackim , port lotniczy .
Ośrodek przemysłowy , głównie samochodowy ( GAZ ) , maszynowy , chemiczny , metalurgiczny , stoczniowy .
Historia .
Założony został w 1221 jako gród obronny księstwa włodzimierskiego .
Stał się dużym ośrodkiem handlowo-kulturowym .
Od 1350 roku stał się stolicą księstwa suzdalsko-niżnonowogrodzkiego , w 1392 roku włączony został do państwa moskiewskiego .
Na początku XVII w. stał się jednym z najbogatszych miast Rosji .
W latach 1611–1613 był punktem oporu Rosjan w walce z Rzeczpospolitą w wojnie moskiewskiej .
W 1777 roku stał się stolicą guberni , a od 1825 roku był siedzibą generała-gubernatora powołżskiego .
W 1902 w mieście miała miejsce wielka demonstracja robotników przedstawiona w powieści `` Matka '' Maksyma Gorkiego .
W roku 1917 był jednym z ośrodków bolszewików .
W 1932 roku miasto zostało nazwane Gorki , dla uczczenia powrotu z Włoch do Moskwy na zaproszenie Józefa Stalina pisarza Maksyma Gorkiego .
W 1936 roku miasto Gorki zostało stolicą obwodu .
Od 1929 roku zbudowano w mieście wielkie zakłady samochodowe GAZ – Gorkowskij Awtomobilnyj Zawod ( po rosyjsku : Горьковский Автомобильный Завод ) we współpracy z amerykańskim Fordem .
Powstała nowa modelowa dzielnica mieszkaniowa oraz fabryka .
Wielu amerykańskich robotników zgłosiło się do pracy w tych zakładach , kierując się swoimi lewicowymi sympatiami .
Reakcja na kontrast ich wyobrażeń o ZSRR z rzeczywistością na miejscu oznaczała dla szeregu z nich zesłanie na Syberię .
