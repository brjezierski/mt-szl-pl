Niżny Tagił
Niżny Tagił ( – Niżnij Tagił ) – miasto w azjatyckiej części Rosji , w obwodzie swierdłowskim , nad Tagiłem ( dorzecze Obu ) , ok. 25 kilometrów na wschód od umownej granicy Europy i Azji .
Jeden z największych i najstarszych ośrodków górniczych w rejonie Uralu , wydobywane są tam rudy miedzi i żelaza .
