Niebuszewo
Niebuszewo ( do 1945 niem .
`` Zabelsdorf '' ) – część miasta i osiedle administracyjne Szczecina , będące jednostką pomocniczą miasta , położone w dzielnicy Północ .
Według danych z 2021 r. na osiedlu na pobyt stały zameldowanych było 16 457 osób .
Położenie .
Część miasta Niebuszewo jest przedzielone linią kolejową na dwie części , gdzie część północna , należąca do dzielnicy Północ , to Niebuszewo właściwe , które w większości zajmowane jest przez spółdzielcze osiedle Książąt Pomorskich oraz osiedle bpa Bandurskiego .
Za to część południowa należy administracyjnie do osiedla Niebuszewo-Bolinko w dzielnicy Śródmieście .
Samorząd mieszkańców .
Rada Osiedla Niebuszewo liczy 15 członków .
W wyborach do rad osiedli 20 maja 2007 roku udział wzięło 413 głosujących , co stanowiło frekwencję na poziomie 1,03 % ( najmniejszą spośród osiedli Szczecina ) .
W wyborach do rady osiedla 13 kwietnia 2003 udział wzięło 105 głosujących , co stanowiło frekwencję 2,80 % .
Samorząd osiedla Niebuszewo został ustanowiony w 1990 roku .
