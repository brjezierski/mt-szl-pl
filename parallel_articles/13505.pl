Niechorz
Niechorz – wieś krajeńska w Polsce położona w województwie kujawsko-pomorskim , w powiecie sępoleńskim , w gminie Sępólno Krajeńskie , przy trasie drogi krajowej nr 25 .
Oddalona od Bydgoszczy o 54 km , a od Sępólna Krajeńskiego o 6,2 km .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Historia .
Niechorz wymieniony został po raz pierwszy w roku 1496 jako wieś i młyn .
Od 1739 r. istniał tu folwark .
Do roku 1821 właścicielami Niechorza byli Potuliccy .
Od co najmniej 1754 roku do 1920 działała szkoła ewangelicka .
