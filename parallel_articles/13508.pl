Przebieg .
Jedwabny Szlak prowadził z Xi ’ an , dawnej stolicy Chin , przez Lanzhou do oazy Dunhuang , gdzie rozdzielał się na szlak główny ( północny ) i południowy .
Droga północna prowadziła pomiędzy południowym podnóżem gór Tienszan i północnym skraju pustyni Takla Makan , przez oazy Hami , Turfan , Korla , Kucza i Aksu do Kaszgaru .
Istniał też wariant jeszcze bardziej północny – z Hami , biegnący wzdłuż północnych podnóży Tienszanu przez : Ałmałyk , Bałasagun , Taszkent , Samarkandę i Bucharę .
Droga południowa wiodła przez Czarklik , Czerczen , Niję , Keriję , Hoten i Jarkend do Kaszgaru .
Z Kaszgaru istniało wiele dalszych szlaków , m.in .
