Ascochyta equiseti
Ascochyta equiseti ( Desm . )
Grove – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , saprotrof i pasożyt skrzypu ( `` Equisetum '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ascochyta , Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1846 r. J.B.H .
Desmazières nadając mu nazwę `` Phoma equiseti '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu W.B .
Grove w 1918 r .
Synonimy :
Morfologia .
Na pędach skrzypu powoduje powstawanie ciemnoszarych plam .
W ich obrębie tworzą się ciemnobrązowe do prawie czarnych , owalne lub prawie kuliste pyknidia o rozmiarach 115-130 × 120-145 μm , z okrągłym otworem o średnicy 20-25 μm w średnicy .
Konidia podłużno-elipsoidalna , owalne , o obu końcach zaokrąglonych , proste , z jedną przegrodą , czasami na przegrodzie lekko zwężone .
Mają rozmiary 10-12,5 × 2,5-3,75 μm .
Występowanie .
Opisano jego występowanie w Ameryce Północnej i w wielu krajach Europy na obumierających i martwych pędach skrzypu .
W Polsce po raz pierwszy podały jego występowanie Ewa Połeć i Małgorzata Ruszkiewicz-Michalska w 2011 roku na skrzypie polnym ( `` Equisetum arvense '' ) .
