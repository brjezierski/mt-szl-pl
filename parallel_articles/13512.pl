Niedrzwica ( stacja kolejowa )
Niedrzwica – stacja kolejowa w Niedrzwicy Dużej w powiecie lubelskim , w Polsce .
W roku 2017 stacja obsługiwała 50–99 pasażerów na dobę .
Jest to pierwsza stacja na linii poza granicami Lublina .
Posiada 3 tory główne ( jeden zasadniczy oraz dwa dodatkowe ) .
Jesienią 2011 dokonano przebudowy układu torowego stacji – wybudowano nowy peron wyspowy pomiędzy dotychczasowymi torami nr 1 i 4 .
Tor dodatkowy nr 2 został zlikwidowany , a jego funkcję przejął tor nr 4 .
Peron posiada m.in .
monitoring i wiaty .
Już wcześniej przed budową peronu zlikwidowano rozjazd w torze nr 4 kierujący na tor przy rampie ładunkowej - jedyny możliwy wjazd jest od strony Wilkołazu .
Budynek dworca popadł w kompletną ruinę – jest zamknięty i zdewastowany , a wieża ciśnień została zburzona .
Do roku 2015 ruch prowadzony był przez dwie nastawnie : dysponującą `` N '' ( od strony Lublina ) i wykonawczą `` N1 '' ( od strony Wilkołazu ) , które posiały urządzenia mechaniczne scentralizowane z sygnalizacją świetlną .
Obie obsługiwały przejazdy kolejowo-drogowe z zaporami .
Przed modernizacją przy torach nr 1 i 2 znajdowały się nasypy ziemne zamiast peronów .
Obecnie w nastawni dysponującej znajduje się Lokalne Centrum Sterowania .
Od 2015 roku steruje ono stacjami Wilkołaz oraz Lublin Zemborzyce , jak również urządzeniami SRK w samej Niedrzwicy .
Umożliwiło to ponowne włączenie do eksploatacji wyremontowanej stacji Wilkołaz , dzięki czemu 24-kilometrowy dotychczas szlak Niedrzwica-Kraśnik skrócił się o 9 kilometrów .
Tak długi dystans spowodowany był zamknięciem stacji Leśniczówka i Wilkołaz oraz likwidacją układu torowego na tej pierwszej .
Dnia 9 grudnia 2012 , po zmianie rozkładu jazdy , powróciły pociągi dalekodystansowe .
W 2018 roku przeprowadzono na stacji kolejne prace , m.in .
związane z jej elektryfikacją oraz wymianą podrozjazdnic .
