Niedzica-Zamek
Niedzica-Zamek – wieś w Polsce , położona w województwie małopolskim , powiecie nowotarskim , gminie Łapsze Niżne .
Do 1 stycznia 2014 roku miejscowość nosiła nazwę `` Zamek '' i stanowiła przysiółek wsi Niedzica .
W latach 1975–1998 miejscowość należała do województwa nowosądeckiego .
Miejscowość jest położona nad Zbiornikiem Czorsztyńskim .
Graniczy z Niedzicą , Falsztynem oraz – poprzez Zbiornik Czorsztyński – z Czorsztynem .
Na południowym krańcu wsi , przy granicy z Niedzicą , znajduje się Zamek Dunajec z XIV wieku .
Kultura .
We wsi jest używana gwara spiska , zaliczana przez polskich językoznawców jako gwara dialektu małopolskiego języka polskiego , przez słowackich zaś jako gwara przejściowa polsko-słowacka .
