Ascodichaena rugosa
Ascodichaena rugosa Butin – gatunek grzybów z typu workowców ( `` Ascomycota '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ascodichaenaceae , Rhytismatales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozowany został przez Karola Linneusza jako `` Lichen rugosus '' w 1753 r. Później zaliczany był do różnych rodzajów ( `` Dichaena '' , `` Heterographa '' , `` Hysterina '' , `` Hysterium '' , `` Opegrapha '' , `` Phloeoscoria '' , `` Polymorphum '' , `` Psilospora '' , `` Radulum '' , `` Schizoderma '' , `` Verrucaria '' .
Teleomorfa po raz pierwszy zdiagnozowana została w 1977 r. we Francji na dębie bezszypułkowym .
