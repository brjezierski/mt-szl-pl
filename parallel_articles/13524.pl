Fałszywa Biblia
Fałszywa Biblia ( Biblia cudzołożników , Biblia grzeszników , Biblia Niegodziwa , Zła Biblia ; , rzadziej : lub ) – przyjęte określenie Biblii wydanej w języku angielskim w 1631 roku przez Roberta Barkera i Martina Lucasa , królewskich drukarzy z Londynu , mającej stanowić reprint tzw .
Biblii króla Jakuba .
Określenie wywodzi się z błędu zecerskiego – w tekście dziesięciu przykazań opuszczono słowo `` nie '' w przykazaniu „ Nie cudzołóż ” .
Pomyłka została rozpowszechniona w wielkiej liczbie egzemplarzy , stąd określenie .
Autorzy reprintu zostali w rok później ukarani grzywną w wysokości 300 funtów oraz odebraniem licencji na druk .
Wydanie Biblii z tak rażącym niedopatrzeniem zszokowało osobiście ówczesnego króla Anglii i Szkocji Karola I ( który zamówił wtedy tysiąc kopii Biblii , nie wiedząc o pomyłce ) oraz biskupa Canterbury .
Ten drugi powiedział wówczas :
Z rozkazu króla , zecerzy i wydawcy zostali wezwani przed sąd Izby Gwiaździstej .
Po dokładnej analizie wszystkich wydarzeń dowiedziono winy oskarżonych .
Zasądzona kara była równowartością gospodarstwa o powierzchni 120 ha , stanowi w przybliżeniu ekwiwalent dzisiejszej kwoty 34 000 GBP ( sierpień 2010 ) .
Większość kopii Fałszywej Biblii została natychmiastowo wycofana z użytku , do dzisiaj ostało się jedynie 11 egzemplarzy , z czego jeden znajduje się w Oddziale Książki Rzadkiej New York Public Library i prawie nigdy nie jest udostępniany , a inny można z kolei zobaczyć w Muzeum Biblii w Branson , Missouri ( USA ) .
