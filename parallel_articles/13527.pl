Siedzibą gminy była Nieszawa , która stanowiła odrębną gminę miejską .
Gminę Nieszawa utworzono 1 stycznia 1973 roku w powiecie aleksandrowskim w woj .
bydgoskim .
1 czerwca 1975 roku gmina weszła w skład nowo utworzonego woj .
włocławskiego .
2 kwietnia 1991 roku siedziba gminy została przeniesiona z Nieszawy do Wagańca , z jednoczesną zmianą nazwy gminy na `` gmina Waganiec '' ; równocześnie dołączono do Nieszawy wieś `` Kolonia Nieszawa '' z gminy Waganiec .
