Otorbielak pszczeli
Otorbielak pszczeli ( `` Ascosphaera apis '' ( Maasen ex Claussen ) L.S .
Olive & amp ; Spiltoir ) – gatunek grzybów z rodziny `` Ascosphaeriaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ascosphaera , Ascosphaeraceae , Onygenales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1921 r. Maasen i Peter Claussen nadając mu nazwę `` Pericystis apis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Lindsay Shepherd Olive i
Charles Francis Spiltoir w 1955 r .
Synonimy naukowe :
Znaczenie .
Wywołuje grzybicę otorbielakową ( `` Ascosphaerosis larvae apium '' ) czerwiu pszczelego .
Zarodniki tego grzyba odznaczają się dużą wytrzymałością na niekorzystne warunki środowiska .
