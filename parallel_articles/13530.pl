Niewistka
Niewistka ( w latach 1977–1981 Podrzecze ) – wieś w Polsce położona w województwie podkarpackim w powiecie brzozowskim , w gminie Dydnia .
Leży na lewym brzegu Sanu .
W połowie XIX wieku właścicielem posiadłości tabularnej w Niewistce był Władysław Bobczyński .
Pod koniec XIX właścicielem posiadłości tabularnej w Niewistce był Aleksander Bobczyński .
Mieszkańcy dzielą miejscowość na : `` Wieś '' i `` Huty '' ( popularne wyrażenie : `` idę do wsi '' ) .
W latach 70 XX w. , planowano tutaj budowę zapory , jednak w tamtym czasie zaniechano projektu .
Do końca 2011 r. inwestycja nie została jeszcze wykreślona z planu zagospodarowania przestrzennego województwa podkarpackiego i obecnie coraz częściej mówi się o niej , jako przyszłościowej dla gospodarki wodnej .
Dolina Sanu powyżej Niewistki aż do okolic Sanoka zachowała swój dziewiczy charakter , stanowiąc atrakcję dla kajakarzy ( szlak kajakowy Zwierzyń–Przemyśl ) , wędkarzy oraz amatorów kąpieli .
Wielu turystów latem rozbija swoje namioty nad Sanem , bądź nocleguje w gospodarstwach agroturystycznych .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krośnieńskiego .
