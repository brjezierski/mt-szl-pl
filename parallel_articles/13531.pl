Nieżychowice
Nieżychowice ( do 1950 r. `` Szenfeld '' , niem .
`` Schönfeld '' ) – wieś w Polsce , w województwie pomorskim , w powiecie chojnickim , w gminie Chojnice .
We wsi znajduje się Kościół rzymskokatolicki pw .
Trójcy Świętej oraz Szkoła Podstawowa im .
Żołnierzy Monte Cassino .
Nazwa .
13 lutego 1950 r. zmieniono nazwę miejscowości z Szenfeld na Nieżychowice .
Administracja .
Prywatna wieś szlachecka Szenfeld położona była w drugiej połowie XVI wieku w województwie pomorskim .
W latach 1975–1998 miejscowość należała województwa bydgoskiego .
W okresie II Rzeczypospolitej stacjonowała tu placówka Straży Granicznej Inspektoratu Granicznego nr 7 „ Chojnice ” .
Komunikacja .
Miejscowość jest położona przy linii kolejowej Chojnice – Człuchów – Szczecinek – Runowo Pomorskie , linii kolejowej Tczew – Chojnice – Piła i drodze wojewódzkiej nr 212 .
Połączenie z centrum Chojnic umożliwiają autobusy komunikacji miejskiej ( linia nr LN ) .
