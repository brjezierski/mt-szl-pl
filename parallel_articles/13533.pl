Ōita
– miasto portowe położone w Japonii , na wyspie Kiusiu , ośrodek administracyjny prefektury Ōita .
Miasto leży w północno-wschodniej części wyspy Kiusiu , nad zatoką Beppu ( Morze Japońskie ) .
W mieście znajduje się port handlowy i pasażerski ; Ōita posiada także port lotniczy .
Historia .
Obszar miasta Ōita jest historycznie znany jako , stolica .
W okresie Sengoku ( XV–XVI w. ) potężny klan Ōtomo miał swoją siedzibę w Funai ; obszar prosperował jako kluczowy port wymiany handlowej z Portugalią i chińską dynastią Ming .
Sōrin Ōtomo , znany chrześcijański daimyō , był pierwszym , który zaczął szerzyć na tych terenach kulturę zachodnią .
Funai było pierwszym miastem , w którym wybudowano szpital wzorowany na zachodnich i utworzono pierwszy japoński chór .
Gospodarka .
W latach 1960 i 1970 wzdłuż wybrzeża zatoki Beppu powstał okręg przemysłowy .
Wśród zakładów powstałych w okręgu były firmy Nippon Steel i Shōwa Denkō .
W latach 70 .
XX wieku firmy Toshiba i Canon wybudowały i poszerzyły swoje zakłady w strefie śródlądowej .
Do tego czasu miasto stało się głównym centrum produkcji podzespołów i urządzeń elektronicznych .
Obecnie rozwinięte jest hutnictwo żelaza i aluminium , przemysł drzewny , włókienniczy , chemiczny , petrochemiczny oraz celulozowo-papierniczy .
Centrum i dzielnice handlowe znajdują się na północ od stacji Ōita .
Tereny te jednak stopniowo podupadają , ponieważ główne komercyjne obszary zostały rozproszone ze względu na budowę dużych centrów handlowych na przedmieściach .
