Język ulczyjski
Język ulczyjski ( olcza ) – zagrożony wymarciem język z rodziny tunguskiej , używany w Kraju Chabarowskim w Rosji .
Alfabet .
Alfabet ulczyjski został wprowadzony w końcu lat 80 .
