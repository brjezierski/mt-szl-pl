Sianów ( gmina )
Sianów – gmina miejsko-wiejska w północnej części województwa zachodniopomorskiego , w powiecie koszalińskim .
Siedzibą gminy jest miasto Sianów .
Według danych z 31 grudnia 2016 r. gmina miała 13 765 mieszkańców .
Miejsce w województwie ( na 114 gmin ) :
powierzchnia 44. , ludność 29 .
Położenie .
Gmina znajduje się w północnej części województwa zachodniopomorskiego , w północno-wschodniej części powiatu koszalińskiego .
Według danych z 1 stycznia 2009 powierzchnia gminy wynosi 226,77 km² .
Gmina stanowi 13,6 % powierzchni powiatu .
W latach 1975–1998 gmina położona była w województwie koszalińskim .
Sąsiednie gminy :
Do 31 grudnia 2009 r. sąsiadowała z gminą Będzino .
Zmiana nastąpiła po przyłączeniu sołectw Jamno i Łabusz z tej gminy do Koszalina .
