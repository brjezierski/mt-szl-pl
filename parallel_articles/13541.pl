Ściborowice
Ściborowice ( ) – wieś w Polsce , położona w województwie opolskim , w powiecie krapkowickim , w gminie Krapkowice .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
Nazwa wsi prawdopodobnie wywodzi się od imienia Ścibor lub Czcibor .
Nazwa wioski brzmiała w swej historii `` Stiborowic '' , `` Stübendorf '' , `` Stiebendorf '' .
15 marca 1947 r. nadano miejscowości , wówczas administracyjnie należącej do powiatu prudnickiego , polską nazwę `` Ściborowice '' .
Historia .
Po raz pierwszy wieś wzmiankowana jest w dokumencie `` Liber fundationis '' biskupa wrocławskiego z 1305 r. Wioska przez pewien okres należy do posiadłości cystersów z klasztoru w Rud , a następnie właścicielami wsi bywają głównie rycerze .
W 1412 r. miejscowość zostaje wydzierżawiona kolegiacie opolskiej .
Od 1580 r. właścicielem zostaje Jan Lubowsky z Lubowic , a w kolejnych latach Ściborowice trafiają pod władanie rodziny von Holy , później von Schönburg , by w 2 poł .
XVII w. trafić w granice posiadłości von Oppersdorffów , którzy , z przerwą na rzecz rodziny Reyman , będą tu panować do 1945 r. Czas Oppersdorffów był dla miejscowości dobrym czasem , gdyż odbudowano folwark , a także zbudowano gorzelnię i browar .
