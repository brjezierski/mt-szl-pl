Ścibórz
Ścibórz ( niem .
`` Stübendorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Paczków .
Do sołectwa Ścibórz należał przysiółek Frydrychów , od 2009 roku stanowi oddzielne sołectwo .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Opis miejscowości .
Wieś położona jest przy drodze krajowej DK46 nad południowym brzegiem Jeziora otmuchowskiego w miejscu gdzie kończy się zapora ziemna zalewu .
Przed budową w latach trzydziestych XX w. Jeziora Otmuchowskiego wieś miała głównie charakter rolniczy .
Budując zalew oraz kanał ulgi jeziora zajęto znaczną część ziemi uprawnej , a wieś znalazła się w wąskim pasie ziemi między jeziorem a kanałem ulgi .
Obecnie wieś głównie letniskowa z rozwiniętą bazą turystyczną .
Funkcjonuje w niej kilka gospodarstw agroturystycznych , restauracje , kemping i ośrodki wczasowe .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Stibornsdorf '' w szeregu wsi lokowanych na prawie polskim – `` iure polonico '' .
Historia .
W XVII wieku obecna wieś funkcjonowała jako dwie wsie Ścibórz Górny i Dolny .
Wówczas wieś znajdowała się w księstwie nyskim przy drodze łączącej Javornik z Otmuchowem .
W 1664 roku Ścibórz Górny , a w 1666 Ścibórz Dolny , został kupiony przez Jana Henryka Heymanna von Rosenthala , kanonika nyskiego i ołumunieckiego , członka rady biskupów wrocławskich .
Od 1672 roku biskupa pomocniczego i administratora Księstwa Nyskiego .
