Ścigów
Ścigów ( dodatkowa nazwa w j. niem .
Schiegau ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Strzeleczki .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
W alfabetycznym spisie miejscowości na terenie Śląska wydanym w 1830 roku we Wrocławiu przez Johanna Knie wieś występuje pod zniekształconą , polską nazwą `` Schiegow '' oraz niemiecką `` Schiegau '' .
9 września 1947 r. nadano miejscowości polską nazwę `` Ścigów '' .
Historia .
Według spisu ludności z 1 grudnia 1910 , na 471 mieszkańców Ścigowa 16 posługiwało się językiem niemieckim , 449 językiem polskim , 1 innym językiem , a 15 było dwujęzycznych .
Do 1956 roku Ścigów należał do powiatu prudnickiego .
W związku z reformą administracyjną , w 1956 Ścigów został odłączony od powiatu prudnickiego i przyłączony do nowo utworzonego krapkowickiego .
Mieszkańcy .
Miejscowość zamieszkiwana jest przez mniejszość niemiecką oraz Ślązaków .
Mieszkańcy wsi posługują się gwarą prudnicką , będącą odmianą dialektu śląskiego .
Należą do podgrupy gwarowej nazywanej `` Podlesioki '' .
