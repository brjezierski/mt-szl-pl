Siedlanka ( Leżajsk )
Siedlanka – część miasta Leżajsk w południowo-wschodniej Polsce , w województwie podkarpackim , w powiecie leżajskim .
Siedlanka leży na północ od obecnego centrum miasta , wzdłuż ulicy o nazwie `` Siedlanka '' .
