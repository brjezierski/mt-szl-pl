Siedlce
Siedlce – miasto na prawach powiatu we wschodniej Polsce w województwie mazowieckim , na Podlasiu Południowym , pomiędzy rzekami Muchawką i Helenką , historycznie należało do ziemi łukowskiej ( Małopolska ) .
Siedziba rzymskokatolickiej kurii diecezji siedleckiej .
Miasto stanowi lokalne i ponadlokalne centrum gospodarcze ( rozwinięty handel , usługi i przemysł ) , edukacyjne ( szkoły wyższe m.in .
uniwersytet , seminarium duchowne i 10 szkół średnich , szkoła muzyczna I i II stopnia ) i kulturalne ( 2 domy kultury , teatr , 4 biblioteki i 3 muzea ) .
Według danych GUS z 31 grudnia 2021 r. , Siedlce liczyły 76 005 mieszkańców i były pod względem liczby ludności czwartym ( po Warszawie , Radomiu oraz Płocku ) miastem w województwie mazowieckim , a także 46. spośród najludniejszych miast w Polsce .
Położenie .
Siedlce leżą na Wysoczyźnie Siedleckiej , która geograficznie jest częścią Niziny Południowopodlaskiej ; między rzekami Muchawka i Helenka i zajmują powierzchnię 3186 ha .
