Sieradz
Sieradz – miasto w środkowej Polsce , w województwie łódzkim , w powiecie sieradzkim , położone na Nizinie Południowowielkopolskiej , nad Wartą , w Kotlinie Sieradzkiej .
Przez Sieradz biegną drogi krajowe : nr 12 , S8 i nr 83 oraz linia kolejowa nr 14 .
Od XIV w. do II rozbioru Polski oraz w latach 1975–1998 Sieradz był stolicą województwa sieradzkiego .
Sieradz jest ważnym węzłem drogowym i ośrodkiem kulturalno-gospodarczym województwa łódzkiego .
Był miastem królewskim Korony Królestwa Polskiego w starostwie sieradzkim w powiecie sieradzkim województwa sieradzkiego w końcu XVI wieku .
Miejsce obrad sejmików deputackich województwa sieradzkiego od XVI wieku do pierwszej połowy XVIII wieku .
Według danych GUS z 31 grudnia 2021 r. miasto liczyło 40 891 mieszkańców .
Nazwa miasta .
Nazwa miasta pochodzi od imienia `` Sirad '' bądź `` Sierad '' , którego wcześniejszą formą według niektórych etymologów był `` Wszerad '' , według innych zaś – `` Sirorad '' .
Od XII w. nazwa miejscowości notowana była po łacinie jako `` Siradia '' .
W 1136 nazwę zapisano jako `` Ziraz '' , w 1241 – `` Chirask '' , 1261 ( będący już wtedy miastem ) `` Seradz '' , 1280 `` Syradz '' , a w 1284 `` Siraz '' .
Przez następne stulecia używana była nazwa w dwu wariantach : `` Siradz '' oraz `` Sieradz '' .
W roku 1154 arabski geograf Al-Idrisi w swoim dziele pt .
Księga Rogera zamieścił nazwę Sieradza jako `` S ( i ) rad ( i ) ja '' pośród innych polskich miast : Krakowa , Gniezna , Wrocławia , Łęczycy oraz Santoka .
