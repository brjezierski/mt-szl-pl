Sierpuchow
Sierpuchow ( ) – miasto w Rosji w obwodzie moskiewskim ( 100 km na południe od Moskwy ) , nad Oką , w 2020 r. ok. 126 tys .
mieszkańców , stolica rejonu sierpuchowskiego .
Założone w 1339 w celu ochrony południowego przedpola Moskwy .
Zagrożone tatarskimi najazdami miasto posiadało kamienną twierdzę ( ukończoną w 1556 ) .
Wybudowano je w celu zablokowania Tatarom szlaku prowadzącego do Moskwy od południa .
W 1610 roku Sierpuchów został zajęty przez Polaków .
W następnych stuleciach niszczejące mury cytadeli służyły mieszkańcom jako źródło kamienia budowlanego .
Dziś w mieście funkcjonuje przemysł tekstylny i mechaniczny , także produkcja mebli , papieru .
