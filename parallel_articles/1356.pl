Aseroë rubra
Aseroë rubra Labill .
– gatunek grzybów należący do rodziny sromotnikowatych ( `` Phallaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aseroë , Phallaceae , Phallales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Niektóre synonimy nazwy naukowej :
Charakterystyka .
W rodzimej Australii znany jest pod popularną nazwą `` anemone stinkhorn '' albo `` sea anemone fungus '' .
Odznacza się specyficznym kształtem i odrażającym zapachem , który zwabia muchy roznoszące zarodniki grzyba .
Był pierwszym gatunkiem grzyba opisanego z Australii ; w 1800 roku okazy znalezione w południowej Tasmanii opisał francuski botanik Jacques Labillardière .
