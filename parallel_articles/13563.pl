Sitaniec-Kolonia
Sitaniec-Kolonia – wieś w Polsce położona w województwie lubelskim , w powiecie zamojskim , w gminie Zamość .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Wieś stanowi sołectwo gminy Zamość .
Historia .
Miejscowość została założona pod koniec XVIII wieku na gruntach sitańskich Ordynacji Zamojskiej .
W 1785 ordynat Andrzej Zamoyski osadził we wsi 16 rodzin kolonistów niemieckich .
W połowie XIX wieku miejscowość liczyła 20 domów , z których jeden należał do Polaka .
Z czasem osadnicy niemieccy ulegli polonizacji .
W latach 1939–1944 władze okupacyjne prowadziły działania zmierzające do regermanizacji mieszkańców wsi ( Deutschstämmige ) .
