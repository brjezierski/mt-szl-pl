Sitaniec-Wolica
Sitaniec-Wolica – wieś w Polsce położona w województwie lubelskim , w powiecie zamojskim , w gminie Zamość .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Przez miejscowość przepływa Czarny Potok .
Znajduje się tu zabytkowa ceglana dzwonnica .
Wieś stanowi sołectwo gminy Zamość .
Historia .
Wieś została osadzona przez Sitańskich zapewne w połowie XV stulecia na gruntach Sitańca .
Pierwsza wzmianka o niej pochodzi z 1465 r. Początkowo zwana była Wolą Sitańską .
Tak nazywano ją w rejestrze poborowym z 1564 r. ( jako Wolia ) .
Jednakże począwszy od połowy XVIII w. pojawia się nowa nazwa – Wolica : na mapie K. de Perthees ’ , a z 1786 r. oraz w 1893 roku .
W posiadaniu rodu Sitańskich i Ściborów wieś Wola pozostawała do 1583-85 r. , kiedy to przeszła na własność
Zamoyskich .
W roku 1589 została włączona do dóbr Ordynacji Zamojskiej .
