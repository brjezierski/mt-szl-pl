Alfabet śląski ( Pro Loquela Silesiana )
Alfabet śląski w opracowaniu Pro Loquela Silesiana , ślabikŏrzowy szrajbōnek – zmodyfikowany alfabet łaciński służący do zapisu etnolektu śląskiego .
Jest to nowsza propozycja ortografii tego dialektu .
Opracowany został przez Jolantę Tambor i współpracowników ze stowarzyszenia Pro Loquela Silesiana z myślą o młodzieży uczącej się etnolektu śląskiego .
W alfabecie tym ukazał się elementarz „ Gōrnoślōnski ślabikŏrz ” i podręcznik „ Ślabikŏrz niy dlŏ bajtli ” .
