Niektóre znaki mają charakter pandialektalny , czyli mogą być wymawiane w różny sposób w zależności od dialektu .
Ã - [ ã ] , a nosowe ( tylko w niektórych dialektach ) .
Ŏ - [ ou̯ ] , dyftong odpowiadający polskiemu ą i czeskiemu ou .
Ō - [ o~u ] , dźwięk pośredni międzu u a o .
Ô - [ u̯o ] , dyftong odpowiadający słowackiemu ô. Samogłoska ta jest realizowana charakterystycznie dla danego dialektu .
