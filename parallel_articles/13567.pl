Płacz ( brytyjski miniserial )
Płacz – czteroodcinkowy serial telewizyjny produkcji brytyjskiej stacji telewizyjnej BBC One , który jest luźną adaptacją powieści o tym samym tytule autorstwa Helen FitzGerald .
Serial był emitowany od 30 września 2018 roku do 21 października 2018 roku przez BBC One .
Natomiast w Polsce był od 23 października 2018 roku do 30 października 2018 roku przez Canal+ Seriale
Serial opowiada historię Joanny Lindsay , której dziecko zostało porwane w Australii .
Produkcja .
24 sierpnia 2017 roku , stacja BBC One ogłosiła zamówienie mini serialu .
