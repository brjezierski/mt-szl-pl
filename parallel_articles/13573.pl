Oko ( go )
– w go oznacza niezajęty punkt otoczony przez kamienie tego samego koloru .
Oko może również znajdować się w rogu ( punkcie 1-1 ) lub na brzegu planszy .
Jest to jedno z najważniejszych pojęć w go , ponieważ jedno oko daje dużą przewagę w semeai , a grup posiadających dwoje oczu nie można zabić .
Pomimo tego , nie ustalono jego precyzyjnej definicji .
Często tą samą nazwą określa się obszary zawierające więcej niż jedno wolne pole .
Ten brak jednoznaczności utrudnia rozpoznawanie fałszywych oczu w programach grających w go .
Czasami też potencjalne oko określa się mianem pół-oka , a kształt zawierający wiele potencjalnych oczu niekiedy nazywa się ocznym .
