Śląska Kawiarnia Naukowa
Śląska Kawiarnia Naukowa – stowarzyszenie koordynowane przez Tomasza Rożka .
Jednym z projektów stowarzyszenia są comiesięczne spotkania służące popularyzacji nauki .
Stowarzyszenie organizuje też interaktywną wystawę edukacyjną `` Dolina Węgla '' .
Spotkania Kawiarni odbywają się w klubie Oko Miasta znajdującym się w Katowickim Rondzie Sztuki raz w miesiącu , najczęściej w czwartek .
Spotkania odbywają się w luźnej atmosferze , przy ciastku i kawie .
Podczas spotkań ŚKN Tomasz Rożek rozmawia z zaproszonym gościem na dany temat .
Co miesiąc poruszane jest inne zagadnienie .
Zapraszani goście to często znani polscy naukowcy .
Na każdym spotkaniu jest krótka przerwa , na której można posłuchać muzyki na żywo .
Pod koniec spotkania publiczność może zadawać pytania .
Kawiarnia Naukowa wspierana jest finansowo przez Urząd Miasta Katowice .
Jej partnerami są : National Geographic Society i Uniwersytet Śląski w Katowicach .
Spotkania .
Pierwsze spotkanie Kawiarni odbyło się 4 października 2007 roku .
Gościem był Marcin Ryszkiewicz .
Rozmowa dotyczyła ewolucji i nosiła tytuł `` Ewolucja - od bezmyślnej komórki do bezmyślnego człowieka '' .
Nagrania z części spotkań dostępne są na kanale Kawiarni na youtube .
