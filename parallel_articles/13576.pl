Górali śląskich zwano Jabłonkowianami od pasma Jabłónek .
Przy czym nazwę miasta Jabłonków ( pierwotnie być może Jabłonka ) wywodzi się od `` jabłonki '' ≤ `` jabłoni '' , tudzież od gruszyczki .
Od 1947 r. w pierwszy weekend sierpnia w lasku miejskim w Jabłonkowie odbywają się Międzynarodowe Spotkania Folklorystyczne „ Gorolski Święto ” .
Impreza plenerowa trwa 3 dni i prezentuje się na niej kulturę ludową , którą organizatorzy chcą przedstawić jako źródło tożsamości i żywą wartość obecnych czasów .
Tożsamość .
W polskiej części Śląska Cieszyńskiego liczne kontakty z mieszkańcami dawnego Śląska pruskiego i tamtejszym znaczeniem słowa gorol doprowadziły do zauważalnej zmiany tożsamości góralskiej , która przestała iść w parze z tożsamością śląską .
Zupełnie inaczej sytuacja wygląda na tzw .
Zaolziu , gdzie równoczesne samookreślenie się jako `` gorol '' nie jest sprzeczne z identyfikacją śląską , wręcz ją wzmacnia .
Określeniem `` Gorol '' potocznie nazywa się Gorolski Święto .
Nazwę `` Gorol '' nosi również regionalny chór męski z Jabłonkowa .
