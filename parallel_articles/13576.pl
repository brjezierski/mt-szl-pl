Górale śląscy
Górale śląscy ( , gw .
`` Gorole '' ) – grupa etnograficzna ludności polskiej , zamieszkująca pierwotnie tereny Beskidu Śląskiego i Beskidu Śląsko-Morawskiego w granicach dawnego Księstwa Cieszyńskiego .
Podział .
Oprócz wielu cech wspólnych z szeroko pojętą góralszczyzną Karpat Zachodnich , wypływających z głównej formy gospodarki – szałaśnictwa , charakteryzuje się zespołem elementów kultury duchowej i materialnej ( gwara , wierzenia , obyczaje , strój itp .
) , wyróżniającą ją spośród innych grup górali karpackich .
