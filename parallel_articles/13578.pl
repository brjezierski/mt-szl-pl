16 lipca 1922 r. podpisano w Katowicach dokument upamiętniający przejęcie części Śląska przez Rzeczpospolitą Polską .
W tym czasie na terenach przyznanych Niemcom rozpoczął się odwet na tych Ślązakach , którzy w okresie plebiscytowym działali na rzecz Polski .
