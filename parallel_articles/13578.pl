Powstania śląskie
Powstania śląskie – trzy konflikty zbrojne na Górnym Śląsku , które miały miejsce w latach 1919–1921 między ludnością polską i niemiecką .
Odbyły się one w okresie formowania się Państwa Polskiego po zakończeniu I wojny światowej .
I powstanie śląskie .
I powstanie pod wodzą Alfonsa Zgrzebnioka wybuchło samorzutnie 16 sierpnia 1919 r. w związku z aresztowaniem śląskich przywódców Polskiej Organizacji Wojskowej i niezadowoleniem ludności polskiej z terroru i represji niemieckich , których przejawem była m.in .
masakra w Mysłowicach .
Powstanie objęło głównie powiaty pszczyński i rybnicki oraz część okręgu przemysłowego .
Powstanie stłumione zostało przez Niemców do 24 sierpnia 1919 r. Rząd Polski , który był zaangażowany militarnie w wojnę polsko-bolszewicką , nie mógł wesprzeć powstania .
Jedynym celem , jaki osiągnięto , było zmuszenie władz niemieckich do ogłoszenia amnestii , zaś na obszarze plebiscytowym wciąż panowały siejące terror bojówki .
II powstanie śląskie .
II powstanie pod wodzą Alfonsa Zgrzebnioka wymierzone było w niemiecką dominację we władzach administracyjnych prowincji .
Celem było również zapewnienie bezpieczeństwa ludności polskojęzycznej .
Wybuchło po licznych aktach terroru ze strony niemieckiej .
Powstanie objęło swym zasięgiem cały obszar okręgu przemysłowego oraz część powiatu rybnickiego .
Komisja Międzysojusznicza zażądała wstrzymania walk , lecz Zgrzebniokowi udało się uzyskać dostęp Polaków do tymczasowej administracji , likwidację znienawidzonej przez Polaków niemieckiej policji Sipo oraz udział w nowych organach bezpieczeństwa .
W ten sposób cele powstania zostały osiągnięte , zaś Polacy uzyskali znacznie lepsze warunki do kampanii przed plebiscytem .
III powstanie śląskie .
III powstanie , pod wodzą Wojciecha Korfantego , miało na celu poprawę niekorzystnego dla Polaków wyniku Plebiscytu na Górnym Śląsku .
Na mocy postanowień traktatu wersalskiego decyzję , do którego państwa należeć będzie Górny Śląsk , oddano w ręce ludności mającej się wypowiedzieć w 1921 r. W plebiscycie większość ludności ( 59,6 % ) opowiedziała się za Niemcami .
Na życzenie Polski dopuszczono do głosowania Górnoślązaków mieszkających poza granicami Śląska , ponieważ liczono na głosy Górnoślązaków z Belgii , Zagłębia Ruhry i Zagłębia Dąbrowskiego .
W konsekwencji głosy tych ludzi zaważyły na wygraniu plebiscytu przez Niemcy .
III powstanie śląskie wybuchło w nocy z 2 na 3 maja 1921 r. i było największym zrywem powstańczym Ślązaków w XX w. W czasie III powstania doszło do dwóch dużych bitew powstańców z Niemcami w rejonie Góry Świętej Anny i pod Olzą .
Były to najkrwawsze starcia w czasie powstań śląskich .
W 1922 r. podpisano w Genewie konwencję w sprawie Śląska , według której obszar przyznany Polsce powiększony został do ok. ⅓ spornego terytorium .
Konwencja była korzystna dla Polski , jeśli chodzi o istniejące na nim obiekty przemysłowe .
Polsce przypadło 50 % hutnictwa i 76 % kopalń węgla .
Włączenie części Górnego Śląska do Polski .
Po wydaniu decyzji dotyczącej podziału Górnego Śląska polskie stronnictwa polityczne i organizacje zawodowe utworzyły Naczelną Radę Ludową z Józefem Rymerem na czele .
