Księstwa śląskie
Księstwa śląskie ( , ) – liczne mniej lub bardziej samodzielne państwa feudalne istniejące na Śląsku od XII do XX wieku , formalnie w ramach księstwa śląskiego .
Powstały poprzez kolejne podziały jednolitego początkowo Księstwa .
Na przestrzeni wieków były zależne od : Polski , Czech ( Kraje Korony Czeskiej ) i Monarchii Habsburgów , później od Prus oraz II Rzeszy .
Do 1806 roku Śląsk był także częścią Świętego Cesarstwa Rzymskiego .
W 1742 roku Śląsk został podzielony na część pruską i austriacką ( Śląsk Austriacki ) .
