Śląskie niebo
Śląskie niebo ( inne nazwy to śląski raj , śląski przysmak ; niem .
nazwa to `` Schlesisches Himmelreich '' , zaś po śląsku to `` Schläsches Himmelreich '' ) – mięsna potrawa regionalna kuchni śląskiej przygotowywana przede wszystkim na świąteczne okazje .
Podawana dawniej także jako rekompensata za nieodpłatną pracę podczas skubania pierza i pracy przy kądzieli .
Głównym produktem wykorzystywanym przy wykonywaniu potrawy jest gotowane , uwędzone lub peklowane mięso wieprzowe ( ewentualnie wieprzowina świeża ) z sosem z suszonych owoców .
Doprawiana w zależności od wariantu powidłami , cynamonem , cytryną , mąką i cukrem .
Sposób przygotowania .
Suszone owoce moczy się przez całą noc w zimnej wodzie .
Następnie wieprzowinę gotuje się około godziny , po czym dodaje namoczone owoce razem z płynem , w którym się moczyły i dogotowuje .
Mięso należy wyjąć .
Z zachowanego wywaru sporządza się sos , dodając do niego ( w celu zagęszczenia ) zasmażkę lub gęste powidła .
Przygotowany sos doprawia się do smaku cukrem , solą , cynamonem , utartą skórką i sokiem z cytryny ( przyprawy w zależności od wariantu potrawy ) .
Mięso kroi się w grube plastry lub na duże kawałki , następnie układa na talerzu z kluskami , polewa sosem i ozdabia owocami .
Danie podaje się na ciepło najczęściej z buchtami lub kluskami .
Potrawa w programach kulinarnych .
W jednym z odcinków `` Kuchni po śląsku '' w Telewizji Silesia Remigiusz Rączka zaprezentował wersję tej potrawy ze świeżej wieprzowiny i gruszek klapsów .
Robert Makłowicz pokazał krok po kroku jak przyrządzić i podać śląskie niebo ( z wędzonej wieprzowiny z suszonymi owocami ) w programie kulinarno-podróżniczym Telewizji Polskiej pt .
