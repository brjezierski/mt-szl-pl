Śląska Partia Regionalna
Śląska Partia Regionalna ( ŚPR , śl .
`` Ślōnskŏ Partyjŏ Regiōnalnŏ '' ) – polska partia polityczna , skupiająca regionalistów ze śląskich organizacji regionalnych .
Została założona 21 czerwca 2017 , zarejestrowana 12 kwietnia 2018 , a wyrejestrowana 11 stycznia 2023 .
W partii skupiono członków Ruchu Autonomii Śląska , Związku Górnośląskiego , Ślōnskiej Ferajny , Demokratycznej Unii Regionalistów Śląskich , Związku Ślązaków , Przymierza Śląskiego oraz niezależnych samorządowców i lokalnych liderów .
Historia .
Utworzenie partii zapowiedzieli 21 czerwca 2017 członek zarządu województwa śląskiego , radny sejmiku tego województwa i wiceprzewodniczący Ruchu Autonomii Śląska Henryk Mercik oraz prezes Związku Górnośląskiego Grzegorz Franki .
Ten drugi jednak w listopadzie 2017 wycofał się z udziału w inicjatywie , w związku z przystąpieniem do Platformy Obywatelskiej .
Liderami ŚPR zostali Henryk Mercik i Ilona Kanclerz , a rzecznikiem prasowym Janusz Dubiel .
Tymczasowo funkcję przewodniczącego zarządu głównego objął Waldemar Murek .
Czołowym działaczem partii został także przewodniczący RAŚ Jerzy Gorzelik .
Wniosek o rejestrację partii złożono 13 października 2017 , a sąd dokonał rejestracji 12 kwietnia 2018 .
Cztery dni później w Katowicach odbyła się konwencja programowa ŚPR .
W maju tego samego roku klub radnych RAŚ w sejmiku przekształcono na klub ŚPR .
12 czerwca wybrano władze partii .
Powołano 15-osobową radę polityczną , której przewodniczącym został Henryk Mercik , a wiceprzewodniczącym Joachim Otte .
W jej składzie znaleźli się też m.in .
Dietmar Brehmer i Marian Makula .
