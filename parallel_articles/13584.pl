Jaskółka Śląska
Jaskółka Śląska ( Ślōnskŏ Szwalbka , Schlesische Schwalbe , Slezská Vlaštovka , Silesian Swallow ) – czasopismo wydawane przez Ruch Autonomii Śląska .
Miesięcznik dwunastostronicowy formatu A3 , kolorowy , zamieszcza artykuły w języku polskim i etnolekcie śląskim ; nr .
Ukazuje się od 1991 z krótkimi przerwami .
W latach 1995-1998 redaktorem pisma był Rudolf Kołodziejczyk , a redakcja mieściła się w Rybniku .
W latach 1999-2000 „ Jaskółkę ” opracowuje Kolegium Redakcyjne .
W latach 2001-2003 redaktorem naczelnym zostaje Tadeusz B. Hadaś .
W tych latach do wydawania „ Jaskółki Śląskiej ” w znaczny sposób przyczyniał się Roman Boino z Miasteczka Śląskiego .
W latach 2003-2004 `` Jaskółkę '' znowu redaguje Kolegium ; nie ma redaktora naczelnego , ale opiekę nad pismem przejmuje Roman Boino .
Na początku 2004 roku opiekunem pisma zostaje Andrzej Roczniok .
W lipcu 2004 roku pismo przejmują Marek Marcisz oraz współtwórca Arkadiusz Faruga , którzy przenoszą redakcję do Chorzowa .
W kwietniu 2007 funkcja redaktora naczelnego powierzona zostaje Marcinowi Melonowi .
Zmienia się na obecny format czasopisma oraz szata graficzna .
Nakład podniesiony zostaje do trzech tysięcy egzemplarzy .
Grupą docelową stają się odbiorcy uniwersalni , a nie tylko członkowie RAŚ .
Rozbudowana zostaje sieć punktów kolportażu .
W roku 2010 redaktorem naczelnym zostaje Dariusz Dyrda .
Pierwszy w historii zawodowy dziennikarz i doświadczony redaktor wprowadza kolejne zmiany , mające na celu uczynienie z Jaskółki nowoczesnego miesięcznika .
Od grudnia 2011 r. obowiązki redaktora naczelnego przejmuje Monika Kassner .
