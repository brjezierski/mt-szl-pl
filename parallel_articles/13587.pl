Oborniki Śląskie ( gromada )
Oborniki Śląskie ( alt .
zapis `` Oborniki Śl . '' )
– dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Oborniki Śl .
z siedzibą GRN w mieście Obornikach Śl .
( nie wchodzącym w skład gromady ) utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie trzebnickim w woj .
wrocławskim , na mocy uchwały nr 29/54 WRN we Wrocławiu z dnia 2 października 1954 .
W skład jednostki weszły obszary dotychczasowych gromad Kuraszków , Możęcin Wielki , Siemianice i Świerzów ze zniesionej gminy Oborniki Śl .
oraz Wilkowa Wielka ze zniesionej gminy Skokowa w tymże powiecie .
Dla gromady ustalono 14 członków gromadzkiej rady narodowej .
1 stycznia 1960 do gromady Oborniki Śl .
włączono wsie Osola i Wielka Lipa ze zniesionej gromady Osolin w tymże powiecie .
31 grudnia 1961 z gromady Oborniki Śl .
wyłączono wieś Kopaszyn , włączając ją do gromady Prusice w tymże powiecie ; do gromady Oborniki Śląskie włączono natomiast wsie Niziny , Lubnów , Jajków , Nowosielce i Zalesie ze zniesionej gromady Uraz oraz wsie Kowale , Piekary , Droszów , Przecławice , Borkowice i Wilczyn ze zniesionej gromady Kowale tamże .
1 stycznia 1972 do gromady Oborniki Śląskie włączono obszar zniesionej gromady Pęgów ( oprócz wsi Ozorowice i Szewce ) w tymże powiecie , tzn .
wsie Pęgów , Golędzinów , Kotowice , Zajączków , Paniowice , Uraz i Raków .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 powiecie trzebnickim reaktywowano gminę Oborniki Śląskie .
