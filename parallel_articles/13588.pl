Położenie .
Ząbkowice Śląskie położone są w południowej części województwa dolnośląskiego w powiecie ząbkowickim , którego są stolicą .
Miasto leży na Przedgórzu Sudeckim nad Budzówką , która jest lewym dopływem Nysy Kłodzkiej .
Według danych z 1 stycznia 2011 powierzchnia miasta wynosiła 13,67 km² .
Ząbkowice Śląskie historycznie leżą na Dolnym Śląsku .
W latach 1975–1998 miasto administracyjnie należało do woj .
