Historia .
Początki Ząbkowic Śląskich .
Dokładna data powstania Ząbkowic Śląskich nie jest znana , jednak z przekazów pośrednich wynika , że stało się to ok. 1280 .
Najstarszy dokument , w którym wymienia się `` civitas Frankenstein '' nosi datę 10 stycznia 1287 .
Ząbkowice powstały koło starszej wsi Sadlno ( obecnie w granicach miasta ) , dokładnie w miejscu , gdzie krzyżowały się ważne drogi handlowe : trakt z Wrocławia do Pragi z drogą biegnącą z biskupiej Nysy do Świdnicy .
