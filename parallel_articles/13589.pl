Kluski śląskie
Kluski śląskie ( kluski białe , biołe , guminowe , gumione , kartofelmelklezy , gumiklyjzy ) – rodzaj klusek ziemniaczanych przygotowanych z gotowanych ziemniaków i mąki ziemniaczanej , formowane w mocno spłaszczone kulki z wgłębieniem , gotowane w osolonym wrzątku .
Kluski śląskie wpisane są na `` Listę produktów tradycyjnych '' województwa opolskiego oraz osobno śląskiego .
Są popularną potrawą na Dolnym i Górnym Śląsku .
Charakterystyka .
Kluski śląskie przygotowuje się z ugotowanych ziemniaków połączonych w stosunku objętościowym 4:1 lub 3:1 z mąką ziemniaczaną z dodatkiem soli ( do smaku ) .
Opcjonalnie dodaje się także surowe jajka .
Najczęściej gotowe kluski mają około 4-5 cm szerokości i 2,5 cm wysokości .
W zależności od użytego gatunku ziemniaków i koloru żółtka jajka , przyjmują barwę białokremową , lekko żółtą a nawet koloru żółtka .
Wgłębienie w klusce oprócz funkcji dekoracyjnej spełnia rolę miejsca , w którym zatrzymuje się podany z potrawą sos .
„ Dziurka ” pomaga także równomiernie ugotować kluski .
Jeszcze gorące , ugotowane ziemniaki przeciska się przez praskę lub mieli w maszynce po czym łączy z pozostałymi składnikami .
Z wyrobionego ciasta formuje się wałek , który następnie kraja się na kawałki , którym następnie nadaje się pożądane kształty .
Charakterystyczną „ dziurkę ” robi się palcem .
Uformowane w ten sposób kluski gotuje się w osolonej wodzie .
Sposób podawania .
Kluski śląskie podaje się zwłaszcza jako dodatek do pieczonych dań mięsnych z sosem ( na Górnym Śląsku zwłaszcza z roladami ) lub przyrządzanej na rozmaite sposoby kapusty .
Występują także jako danie podstawowe , okraszone stopioną słoniną .
Kluski śląskie można podawać również na słodko z dodatkiem śmietany i cukru , bądź dżemu .
Na Górnym Śląsku , gdzie znane są kluski białe i czarne , podawane są one do obiadu na osobnych półmiskach .
