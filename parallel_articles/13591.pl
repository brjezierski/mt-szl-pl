Beskid Śląski
Beskid Śląski ( czes .
`` Slezské Beskydy '' , czasem również `` Těšínské Beskydy '' , 513.45 ) – pasmo górskie , stanowiące część Beskidów Zachodnich .
Jest mezoregionem wchodzącym w skład prowincji Karpat Zachodnich .
Najwyższymi szczytami Beskidu Śląskiego są Skrzyczne ( 1257 m n.p.m . )
i Barania Góra ( 1220 m n.p.m .
) , natomiast dla części czeskiej Czantoria Wielka ( 995 m n.p.m .
) .
Położenie .
Beskid Śląski graniczy z Beskidem Śląsko-Morawskim na zachodzie , Beskidem Żywieckim na południowym wschodzie , Kotliną Żywiecką na wschodzie , Beskidem Małym na północnym wschodzie i Pogórzem Śląskim na północy .
Do 1968 roku góry leżące na zachód od rzeki Olzy zaliczano do Beskidu Śląskiego , potem zaś wyodrębniono je jako Beskid Śląsko-Morawski .
Granicą pomiędzy Beskidem Śląskim a Śląsko-Morawskim jest Przełęcz Jabłonkowska oraz doliny spływającego spod przełęczy na północ potoku Osetnica ( czes .
`` Ošetnice '' ) i rzeki Olzy .
Na terenie Słowacji granicą między Beskidem Śląskim a Beskidem Żywieckim ( w nomenklaturze słowackiej : Beskidami Kisuckimi ) jest spływająca spod Przełęczy Zwardońskiej Skaliczanka ( Skaliťanka ) , w dolnym biegu nosząca nazwę Čierňanki .
Na terenie Polski granicą pomiędzy Beskidem Śląskim a Beskidem Żywieckim jest Przełęcz Zwardońska , potok Czerna , a następnie Soła .
Granicą pomiędzy Beskidem Śląskim a Beskidem Małym jest Brama Wilkowicka , a dalej na północy rzeka Biała .
Pomiędzy Bramą Wilkowicką a doliną Soły poniżej Węgierskiej Górki Beskid Śląski sąsiaduje od wschodu z Kotliną Żywiecką .
Biorąc pod uwagę krainy historyczne , większa część Beskidu Śląskiego ( części zachodnia , centralna i północna ) wchodzi w obszar Śląska Cieszyńskiego ) .
Części wschodnia i południowo-wschodnia to tereny Małopolski ( a konkretniej tzw .
ziemi żywieckiej lub Żywiecczyzny ) .
Zupełnie niewielki fragment południowy ( na południe od głównego wododziału karpackiego ) wchodził w skład tzw .
ziemi czadeckiej .
Klimat .
Beskid Śląski , podobnie jak całe Beskidy Zachodnie charakteryzuje się opadami wynoszącymi od 800 do 1200 mm rocznie i dużym zaśnieżeniem w miesiącach zimowych .
Największy wpływ na kształtowanie się pogody wywierają masy powietrza polarno-morskiego z przeważającym wiatrem zachodnim .
Temperatura powietrza obniża się wraz ze wzrostem wysokości n.p.m .
stopniowo ku południowemu wschodowi .
Średnia roczna temperatura waha się od 5,4 C w partiach grzbietowych do 8,5 C w dolinie rzeki Olzy .
Najchłodniejszym miesiącem jest styczeń , natomiast najcieplejszym jest lipiec .
Istotnym czynnikiem klimatycznym jest silny wiatr , który jest łagodzony przez otaczające góry .
W porze wiosny i jesieni wieje tu wiatr halny .
W partiach szczytowych Baraniej Góry , Równicy i Czantorii przeważają wiatry zachodnie i północno-zachodnie .
W okolicy Istebnej wiatrów zachodnich jest mniej , dlatego też notuje się tu mniejszą ilość opadów atmosferycznych .
