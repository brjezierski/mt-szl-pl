Śląska Jesień Gitarowa
Śląska Jesień Gitarowa – festiwal muzyki gitarowej , odbywający się co dwa lata w Tychach .
Pierwsza edycja miała miejsce w 1986 .
Pomysłodawcą festiwalu był Józef Powroźniak , rektor Akademii Muzycznej w Katowicach .
W programie festiwalu znajdują się recitale światowej klasy gitarzystów , koncerty gitarowe z towarzyszeniem orkiestry , konkurs gitarowy im .
Jana Edmunda Jurkowskiego oraz towarzyszące wydarzeniu wystawy i sprzedaż sprzętu i wydawnictw .
Imprezy festiwalowe odbywają się m.in .
w Teatrze Małym i tyskiej Szkole Muzycznej .
Historia .
Pierwsza edycja miała miejsce w dniach 2–9.10.1986 roku .
Regulamin konkursu zakładał wykonanie w pierwszym etapie jednego z dwu utworów kompozytorów polskich XIX wieku : E. Horeckiego – Wariacje z introdukcją kodą A-dur op .
22 i J.N .
Bobrowicza – Wielkie wariacje na temat Mozarta op .
6 oraz dwudziestominutowego programu dowolnego .
W drugim etapie należało wykonać wybrany przez siebie koncert na gitarę z orkiestrą spośród podanych : J. Rodrigo , Concierto de Aranjuez ; M. Castelnuovo-Tedesco , Koncert D-dur op .
99 ; H. Villa-Lobos , Koncert e-moll ; M. Giuliani , Koncert A-dur op .
30 lub op .
36 .
W przesłuchaniach drugiego etapu solistom towarzyszyła Warszawska Orkiestra Kameralna pod dyrekcją Mirosława Ławrynowicza .
Podczas festiwalu wystąpiła również Orkiestra Państwowej Filharmonii Śląskiej pod dyrekcją Karola Stryi , Państwowa Orkiestra Symfoniczna Filharmonia Górnicza z Zabrza pod dyrekcją Józefa Klimanka , Śląska Orkiestra Kameralna pod dyrekcją Jana Wincentego Hawela wraz z solistą Costasem Cotsiolisem z Grecji dokonała prawykonania Concierto Antillano Ernesto Cordero .
Pierwszą nagrodę zdobyła Nicola Hall z Wielkiej Brytanií , drugą József Eőtvős z Węgier , trzecią Tomasz Spaliński – z klasy Jana Edmunda Jurkowskiego .
