Kołocz śląski
Kołocz śląski – nazwa zwyczajowa ciasta popularnego na Górnym Śląsku .
Równolegle funkcjonuje również określenie `` kołacz śląski '' .
Tradycja .
Kołocz śląski jest regionalnym spadkobiercą tradycji wypieku staropolskiego kołacza i jest nie tylko wyrobem spożywczym , lecz również pieczywem obrzędowym .
Występuje w czterech najpopularniejszych odmianach : kołocz z posypką ( bez nadzienia ) , kołocz z nadzieniem serowym , makowym i jabłkowym .
Tradycja śląska nakazuje równoprawne traktowanie wszystkich odmian jako kołocza śląskiego .
Specyfiką pieczywa jest jego kształt .
W odróżnieniu od staropolskiego okrągłego kołacza , w XIX wieku wykształciła się na Śląsku tradycja wypieku kołocza o kształcie prostokątnym .
Zmiana ta była spowodowana względami praktycznymi – od kiedy stare piece zastąpiono kuchenkami , ze względu na oszczędność miejsca bardziej sprawdzały się blachy prostokątne .
Inną charakterystyczną cechą jest specyficzna posypka odróżniająca go od zbliżonych wypieków z innych części Polski , ale jednocześnie zwiększająca pracochłonność .
Kołocz śląski ma specyficzny maślany zapach , strukturę oraz nieco mniejszą wysokość w porównaniu z innymi wypiekami w pozostałych regionach kraju .
Opis gotowego produktu .
Ciasto jako produkt wypiekany na typowych blachach ma zazwyczaj bezpośrednio po wypieku kształt prostokątny o wymiarach 40 × 60 cm ( ±5 cm ) i wysokości ok. 3,5 cm ( ±0,5 cm ) .
Masa wypieku z jednej blachy wynosi ok. 5–6 kg .
Transport od producenta do punktu sprzedaży odbywa się najczęściej bez krojenia wypieku .
Krojenie może mieć miejsce w punkcie sprzedaży w zależności od woli kupującego .
Zwyczajowo przed spożyciem kołocz jest krojony na mniejsze kawałki o przybliżonych wymiarach 4–6 cm na 4–6 cm .
