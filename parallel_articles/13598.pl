Koń śląski
Koń śląski ( niem .
`` Schlesisches Warmblut '' ) – rasa koni powstała na bazie koni z terenów Dolnego i Górnego Śląska .
Rasa ta odnosi duże sukcesy jako konie zaprzęgowe w dyscyplinie powożenia .
Współcześnie rasa ta występuje w dwóch typach : starym i nowym .
Historia rasy .
Planowa hodowla tych koni rozpoczęła się w XIX wieku w stadach ogierów w Lubiążu i Koźlu .
Miejscowe klacze , mające często orientalnych przodków , kojarzono z ogierami oldenburskimi i wschodniofryzyjskimi , będącymi najmasywniejszymi niemieckimi końmi gorącokrwistymi .
W latach 1880-1939 ukształtowano hodowlę konia w typie konia oldenburskiego , choć szlachetniejszego i `` suchszego '' .
Po II wojnie światowej rasę utrzymano poprzez dolew krwi koni oldenburskich .
Dzięki zamiłowaniu hodowców oraz możliwości użytkowania roboczego w rolnictwie , rasa przetrwała na Śląsku , a także zyskała popularność w Polsce południowo-wschodniej .
Ważnymi ośrodkami hodowlanymi stały się Stada Ogierów w Koźlu , Stado Ogierów w Książu oraz Stadnina koni w Strzegomiu .
Pokrój .
Konie o łagodnym usposobieniu , wydajnych i eleganckich chodach , chętnie pracujące z człowiekiem .
Koń śląski starego typu .
Koń ten odznacza się masywną , lecz harmonijną budową ciała .
Głowa duża , ciężka i koścista , dopuszcza się profil garbonosy .
Ganasze powinny być duże i wyraźne .
Długa i mocno umięśniona szyja , mocna niepodkasana kłoda i mocny grzbiet .
Zad szeroki , umięśniony , lekko skośny lub prosty .
Szeroka i głęboka pierś .
Kończyny o szerokich nadpęciach i `` suchych '' stawach , kopyta proporcjonalne .
Umaszczenie gniade , ciemnogniade , skarogniade i kare , ewentualnie siwe .
Wysokość w kłębie u klaczy : 158–168 cm , u ogierów : 160–170 cm .
