Ruda Śląska
Ruda Śląska ( ) – miasto na prawach powiatu , położone w południowej Polsce , na Górnym Śląsku , w województwie śląskim , w centrum Górnośląskiego Okręgu Przemysłowego .
Ruda Śląska została utworzona 1 stycznia 1959 z połączenia miast Ruda i Nowy Bytom .
Według danych GUS z 31 grudnia 2021 miasto zamieszkiwały 132 766 osoby .
