Podczas sesji Rady Miejskiej 12 stycznia 2006 radni podjęli uchwałę w sprawie utworzenia ponownie dzielnicy Czarny Las , wyodrębniając ją z terenów Wirka , Nowego Bytomia i Bielszowic .
Położenie .
Ruda Śląska znajduje się na Wyżynie Śląskiej w centrum Górnośląskiego Okręgu Przemysłowego ( GOP ) .
Jest jednym z ośrodków centralnych konurbacji górnośląskiej .
Graniczy z Zabrzem , Bytomiem , Świętochłowicami , Chorzowem , Katowicami , Mikołowem i Paniówkami ( w gminie Gierałtowice ) .
Hydrografia .
Miasto leży na dziale wodnym między dorzeczami Wisły i Odry .
Większość wód odprowadzana jest do zlewni Odry przez Kłodnicę oraz jej dopływy : Bytomkę i Potok Bielszowicki ( Kochłówkę ) .
W dzielnicy Chebzie znajdują się źródła Rawy , należącej do dorzecza Wisły .
Na terenie miasta znajduje się też kilka niewielkich antropogenicznych zbiorników wodnych .
Demografia .
Największą populację Ruda Śląska odnotowała w 1991 , kiedy to według danych GUS miasto zamieszkiwało 171 645 mieszkańców .
Od lat 90. liczba mieszkańców systematycznie spada .
Piramida wieku mieszkańców Rudy Śląskiej w 2014 roku .
