Dialekt śląski języka niemieckiego
Dialekt śląski języka niemieckiego albo język śląski ( śl-niem .
, albo ) – zespół gwar , którymi posługiwało się do 1945 roku ok. 7 mln mieszkańców Śląska ( głównie Dolnego ) i graniczącej z nim sudeckiej części Kraju Sudetów .
Przez organizację SIL International dialekt śląski uważany jest za odrębny od języka niemieckiego : „ język dolnośląski ” , nie jest to jednak nazwa geograficznie precyzyjna , gdyż etnolekt ten obejmował również obszary należące do Górnego Śląska .
