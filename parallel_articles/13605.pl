Leksyka dialektu `` Schlesisch '' wykazuje sporo podobieństw z niemieckimi dialektami środkowo- i południowo-zachodnimi , zawiera ponadto zapożyczenia z języków zachodniosłowiańskich ( głównie z polskiego i czeskiego ) .
W dialekcie tym tworzyli m.in .
: Andreas Gryphius , Robert Rößler , Karl Eduard von Holtei i Gerhart Hauptmann .
Dialekty śląskie języków niemieckiego i polskiego , poza zapożyczeniami leksykalnymi , mają ze sobą niewiele wspólnego .
Pierwszy jest dialektem germańskim , drugi dialektem słowiańskim .
