Śmigiel ( stacja kolejowa )
Śmigiel – wąskotorowa stacja kolejowa w Śmiglu , w województwie wielkopolskim , w Polsce położona przy wjeździe do miasta , przy ul .
Leszczyńskiej 14 .
Charakterystyka .
Stacja kolei wąskotorowej na linii Stare Bojanowo Wąskotorowe - Wielichowo , zbudowana została w 1899 r. , z chwilą wydania przez Sejmik powiatu śmigielskiego uchwały zezwalającej na budowę kolei powiatowej o prześwicie toru wynoszącym 1000 mm .
Budynek zbudowany jest na planie kwadratu z muru pruskiego z dwuspadowym dachem krytym dachówką .
Stacja w Śmiglu jest główną stacją `` Śmigielskiej Kolei Dojazdowej '' .
Znajduje się tam nastawnia oraz dyrekcja ŚKD , a także lokomotywownia i warsztaty wraz z podnośnikami .
Na stacji znajduje się również posterunek policji .
Ciekawym elementem śmigielskiego dworca jest zegar na ścianie frontowej budynku .
Otoczenie dworca i cała infrastruktura kolejowa przeszła gruntowny remont w latach 1952 - 1953 , kiedy to decyzją Zarządu Kolei Dojazdowych w Poznaniu zmieniono prześwit toru z 1000 mm na 750 mm , w celu ujednolicenia taboru .
Stacja pełni zarówno funkcje dworca pasażerskiego jak i towarowego .
