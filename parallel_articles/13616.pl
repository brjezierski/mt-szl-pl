Śmierć ( rzeka )
Śmierć ( ; ; ) – rzeka na Białorusi , w obwodzie brzeskim , w rejonie łuninieckim .
Lewy dopływ Prypeci .
Należy do zlewiska Morza Czarnego dorzecza Dniepru .
XIX-wieczny Słownik geograficzny Królestwa Polskiego jako alternatywne nazwy rzeki wymienia `` Smert '' , `` Smuć '' i `` Smut '' .
Przebieg .
Jej źródła znajdują się na krańcu dużego mokradła Błota Hryczyn , w pobliżu miejscowości Miżlessie .
Następnie rzeka płynie na południe , mijając m.in .
Krasną Wolę , Lubaczyn , linię kolejową Kalinkowicze – Łuniniec i Łachwę .
Uchodzi do Prypeci na terenie Rezerwatu Krajobrazowego Środkowa Prypeć , z dala od skupisk ludzkich .
Prowadzona na niej jest turystyka kajakowa .
