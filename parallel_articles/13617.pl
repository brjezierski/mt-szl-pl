Piosenkarz
Piosenkarz , piosenkarka – zawód polegający na wykorzystaniu głosu do śpiewu , z reguły przy akompaniamencie grupy muzycznej .
Utwory wydawane są następnie na płytach ( albumach ) lub wykonywane na koncertach .
Piosenkarze zajmują się muzyką rozrywkową .
Do popularyzacji współczesnych piosenek i ich wykonawców przyczynia się działalność radia i telewizji .
Śpiew bez akompaniamentu instrumentalnego nazywa się a cappella .
