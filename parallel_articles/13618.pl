Śpiew
Śpiew – czynność polegająca na artystycznym operowaniu głosem podczas wykonywania utworu muzycznego ; rodzaj działalności artystycznej człowieka , najstarszy typ wykonawstwa muzycznego .
Dyscyplina zajmująca się nauczaniem śpiewu i szkoleniem głosu zwana jest wokalistyką .
W programie nauczania szkół muzycznych i wyższych uczelni artystycznych dzielona jest na wokalistykę klasyczną ( operową ) i wokalistykę jazzową ( zwaną także rozrywkową lub estradową ) .
Rodzaje śpiewu .
Wyróżnia się śpiew utworów muzycznych z tekstem – śpiew sylabiczny , melizmatyczny – oraz pozbawiony cech mowy – wokaliza .
Natomiast śpiew z zamkniętymi ustami to mormorando ( murmurando ) .
Śpiew może być nieformalny i wykonywany dla przyjemności , a także formalny , taki jak profesjonalny śpiew na przedstawieniu , koncercie bądź w studiu nagraniowym .
Śpiew może być wykonywany w grupie , takiej jak chór , przy akompaniamencie instrumentów muzycznych , pełnej orkiestry lub zespołu .
Śpiewanie bez akompaniamentu muzycznego nazywamy śpiewem a cappella .
