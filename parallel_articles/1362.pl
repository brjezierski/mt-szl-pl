Asia Argento
Asia Argento , właśc .
Aria Maria Vittoria Rossa Argento ( ur .
20 września 1975 w Rzymie ) – włoska aktorka filmowa i telewizyjna , a także reżyserka , scenarzystka i producentka .
Jest laureatką wielu krajowych i zagranicznych nagród filmowych , m.in .
dwóch nagród David di Donatello , będących włoskim odpowiednikiem Oscarów .
Oprócz działalności filmowej zajmuje się również rysowaniem , malowaniem , pisaniem i śpiewaniem , a ponadto tworzeniem w różny sposób teledysków .
Wczesne lata .
Asia Argento urodziła się 20 września 1975 roku w Rzymie .
Jej rodzicami są reżyser Dario Argento oraz aktorka i scenarzystka Daria Nicolodi .
Jest wnuczką producenta Salvatore Argento i pochodzącej z Brazylii fotografki Eldy Luxardo , a także prawnuczką kompozytora Alfreda Caselli .
Ma starszą przyrodnią siostrę Fiore Argento , będącą dzieckiem Daria Argento i jego pierwszej żony Marisy Casale .
Jej przyrodnią siostrą była także Anna Ceroli , córka Darii Nicolodi i rzeźbiarza Maria Ceroliego , która zginęła w wypadku skutera w 1994 roku .
Rodzice dali jej na pierwsze imię „ Asia ” ( wymawia się jako `` Ah-shee-a '' ) , jednak jego zarejestrowania odmówił urząd stanu cywilnego , w związku z czym zostało ono zamienione na „ Aria ” , z którego rejestracją nie było problemu .
Mimo to aktorka zawsze posługiwała się wersją „ Asia ” .
Oboje rodzice Asii specjalizowali się w tworzeniu kina grozy .
Ojciec był odpowiedzialny za nakręcenie jednych z najczarniejszych i najkrwawszych filmów tego gatunku , tj .
m.in .
`` Odgłosy '' , `` Głęboka czerwień '' czy `` Ciemności '' , z kolei matka w wielu z nich grała postacie , które spotykał tragiczny los .
Sama Asia po latach tak to wspominała :
Gdy była dzieckiem , ojciec czytał jej do snu scenariusze swoich filmów , zaś gdy miała 6. urodziny , otrzymała „ prezent ” w postaci możliwości obejrzenia filmu `` Duch '' .
Jako nastolatka goliła głowę i nosiła chłopięce ubrania , zaś o swoim wyglądzie fizycznym wyrażała przekonanie , że „ nie jest lepszy od tego , jaki miał jeden z trupów w filmach ojca ” .
Między 14. a 17. rokiem życia zrobiła sobie większość z obecnie posiadanych tatuaży ( jednym z nich jest imię zmarłej przyrodniej siostry Anny ) .
Z powodu cechującej ją wtedy introwersji zaczytywała się w książkach , co stanowiło sposób na zrekompensowanie braku przyjaciół – w szczególności lubiła dzieła Fiodora Dostojewskiego i Charlesa Baudelaire ’ a .
