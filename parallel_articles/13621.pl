Światowa Federacja Luterańska
Światowa Federacja Luterańska – utworzona w 1947 r. międzynarodowa organizacja zrzeszająca Kościoły wyznania luterańskiego ze wszystkich zakątków świata .
Światowa Federacja Luterańska w 2015 roku zrzeszała 145 Kościołów z 98 krajów , z ponad 74,3 mln wiernych .
Siedziba ŚFL znajduje się w Genewie .
Światowa Federacja Luterańska prowadzi dialog pomiędzy wspólnotami luterańskimi , zawiera porozumienia z innymi wyznaniami , prowadzi akcje ekumenizacyjne i misyjne .
Członkiem Światowej Federacji Luterańskiej jest polski Kościół Ewangelicko-Augsburski w RP .
Przewodniczącym Światowej Federacji Luterańskiej jest ks .
abp dr Musa Panti Filibus ( Luterański Kościół Chrystusowy w Nigerii ) , a sekretarzem generalnym ks .
dr Martin Junge .
