Światło ( czasopismo )
Światło – polski ilustrowany miesięcznik o tematyce fotograficznej , wydawany w Warszawie w latach 1898–1900 .
Historia .
Pierwszy numer miesięcznika Światło wydrukowano w październiku 1898 .
Był czasopismem wydawanym staraniem Jadwigi Golcz – polskiej artystki fotograf , współtwórczyni pierwszej szkoły fotograficznej w Polsce – liczył 48 stron .
Zawartość czasopisma stanowiły aktualne wiadomości fotograficzne , ilustrowane artykuły o fotografii , estetyce w fotografii , artykuły poświęcone technice i chemii fotograficznej oraz opisy sprzętu fotograficznego ( kamery , obiektywy ) .
Wiele miejsca poświęcono na prezentację zdjęć polskich fotografów ( do miesięcznika dołączano wkładki na papierze kredowym – reprodukcje fotografii polskich artystów fotografów ) .
W miesięczniku m.in .
zwracano uwagę na polskie słownictwo fotograficzne ( m.in .
zadbano o jego uporządkowanie ) .
Z czasopismem stale współpracowali m.in .
L. Kossakowski , A. Rzeszotarski , S. Szalay , A. Karoli , M. Wierusz-Kowalski , K. Pruszyński .
Światło było oceniane jako jedno z najpoważniejszych czasopism fotograficznych w ówczesnym czasie – wymienione w V tomie Encyklopedii Powszechnej Orgelbranda , w 1899 roku .
Pierwszym redaktorem prowadzącym miesięcznika Światło był Józef Jerzy Boguski – związany z Towarzystwem Fotograficznym Warszawskim .
