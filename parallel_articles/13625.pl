Świdwin ( stacja kolejowa )
Świdwin – stacja kolejowa w Świdwinie , w województwie zachodniopomorskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2017 stacja obsługiwała 500–699 pasażerów na dobę .
Na stacji zatrzymują się pociągi : REGIO obsługiwane przez spółkę Przewozy Regionalne oraz pociągi IC i TLK spółki PKP Intercity .
7/8 lutego 1946 na stację w Świdwinie dotarł cofnięty ze Szczecina transport Niemców wysiedlanych z Pomorza .
Na skutek niehumanitarnego traktowania przez polską eskortę , z powodu zamarznięcia zginęło 26 osób .
