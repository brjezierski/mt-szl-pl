Świdnik ( dopływ Bobru )
Świdnik ( `` Świnka '' , niem . )
– potok , lewy dopływ Bobru o długości 12,73 km i powierzchni zlewni 37,5 km² .
Potok wypływa w Karkonoszach , pomiędzy Przełęczą Kowarską a Rozdrożem Kowarskim , na wysokości 780 m n.p.m .
Przepływa przez Ogorzelec , Leszczyniec i Szarocin .
Uchodzi w Błażkowej , na granicy z Janiszowem , na wysokości 465 m n.p.m .
Odwadnia północno-wschodnie zbocza Lasockiego Grzbietu , północną część Wzgórz Bramy Lubawskiej i południowo-wschodni fragment Rudaw Janowickich .
Wzdłuż jego górnego biegu prowadzi droga wojewódzka 367 .
