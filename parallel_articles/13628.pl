Świnoujście ( stacja kolejowa )
Świnoujście – stacja kolejowa w Świnoujściu , w województwie zachodniopomorskim .
Według klasyfikacji PKP ma kategorię dworca turystycznego .
Charakterystyka .
Dworzec znajduje w przemysłowej dzielnicy Warszów na wyspie Wolin .
W pobliżu stacji znajduje się przeprawa promowa do centrum miasta , oraz dworzec autobusów dalekobieżnych PKS .
Stacja w pełni jest przystosowana dla osób niepełnosprawnych .
Na stacji zainstalowany jest elektroniczny system zapowiedzi pociągów , którym zarządza spółka PKP PLK .
W pobliżu dworca kolejowego pętle mają linie autobusowe numer : 1 , 5 , 7 , 10 .
Promy obsługujące przeprawę w kierunku do centrum kursują co 20 minut , zaś przyjazdy pociągów są zwykle zsynchronizowane z ich kursowaniem .
W obrębie dworca planowana jest budowa węzła przesiadkowego kolejowo-promowo-autobusowego , w obszarze ulic Dworcowej i Barlickiego , wraz z budową parkingu na 150-200 miejsc .
Inwestycja ta zostanie sfinansowana ze środków Unii Europejskiej .
W Świnoujściu rozpoczyna bieg pociąg pospieszny „ Przemyślanin ” do Przemyśla , który pokonuje trasę 1008 km w niecałe 18 godzin .
Jest to najdłuższa relacja w Polsce , dlatego pociąg ten często nazywany jest polskim Orient Expressem .
