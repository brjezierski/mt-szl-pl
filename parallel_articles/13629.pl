Jadwiga Śląska
Jadwiga Śląska , niem .
`` Hedwig von Andechs '' , `` Hedwig von Schlesien '' ( ur .
między 1178 a 1180 w Andechsie , zm .
14/15 października 1243 w Trzebnicy ) – święta Kościoła katolickiego , żona Henryka I Brodatego , księcia wrocławskiego , matka Henryka II Pobożnego , córka hrabiego Bertolda VI von Andechs , księcia Meranii , fundatorka kościołów i klasztorów .
Życiorys .
Urodziła się i wychowała w zamku Andechs w Bawarii , jako córka Bertolda VI von Andechs i Agnieszki von Rochlitz z rodu Wettynów .
Miała czterech braci , w tym Eckberta , biskupa Bambergu oraz trzy siostry , w tym Agnieszkę ( żonę Filipa II Augusta ) i Gertrudę ( żonę króla węgierskiego Andrzeja II i matkę św .
Elżbiety Węgierskiej ) .
Trzecia z jej sióstr była przełożoną klasztoru benedyktynek w Kitzingen koło Würzburga , dokąd Jadwiga została wysłana w młodym wieku , gdzie zdobyła wykształcenie .
W wieku 12 lat została wydana za mąż za śląskiego księcia Henryka I Brodatego .
Uroczystość ślubna odbyła się najprawdopodobniej w rodzinnym zamku Andechs .
W 1202 roku Henryk został księciem całego Śląska , a w 1233 został księciem Wielkopolski .
W 1229 roku Henryk w wyniku wojny z Mazowszem o ziemię krakowską dostał się do niewoli .
Z pomocą przybyła mu Jadwiga .
W wyniku rozmów z Konradem Mazowieckim Henrykowi zwrócono wolność w zamian za zrzeczenie się roszczeń do Małopolski .
Jadwiga urodziła siedmioro dzieci : Bolesława , Konrada , Henryka , Agnieszkę , Zofię , Gertrudę i Władysława .
Oboje z Henrykiem I byli ludźmi bardzo religijnymi , w 1209 roku złożyli śluby czystości , dbali o rozwój Kościoła i byli fundatorami wielu kościołów , w tym klasztoru sióstr cysterek w Trzebnicy .
Jadwiga chodziła w habicie mniszki , z premedytacją spała na twardym łożu , podczas mrozów chodziła boso , a każdy dzień wypełniała wielogodzinnymi modlitwami .
Gdy zwrócono jej uwagę , aby przynajmniej zimą nosiła buty , posłuchała i od tego czasu związane buty nosiła przewieszone przez ramię .
Nie miała też umiaru w uczestniczeniu we mszach .
W związku z tym powstało nawet łacińskie powiedzenie dotyczące jej pobożności - '' una missa non est contenta ducissa '' ( jedna msza nie zadowoli księżnej ) .
Z powodu silnej wiary planowała przeznaczyć trzy swoje wnuczki na zakonnice i dwóch wnuków na księży , ostatecznie tylko jedna wnuczka i dwóch wnuków ( Władysław wrocławski , Konrad I głogowski ) spełniło jej oczekiwania .
Prowadziła też działalność dobroczynną , starała się o pomoc chorym i ubogim – zorganizowała działalność wędrownego szpitala dla ubogich , otworzyła szpital dla trędowatych w Środzie Śląskiej , kolejną placówkę leczniczą założyła w Trzebnicy .
Po śmierci męża w 1238 zamieszkała w trzebnickim klasztorze , prowadzonym przez jej córkę Gertrudę .
Wkrótce zaangażowała się w ożywienie życia religijnego Śląska , sprowadzając do tamtejszych kościołów duchownych z Niemiec .
Wspierała też sprowadzanie niemieckich osadników na słabo wówczas zaludnione rejony zachodniej części Śląska , wspierając tym samym rozwój rolnictwa .
W czasie najazdu tatarskiego w 1241 r. Jadwiga Śląska schroniła się na zamku w Krośnie Odrzańskim , dokąd najeźdźcy już nie dotarli .
Od wielu lat w tym właśnie mieście w województwie lubuskim , organizowany jest turniej rycerski „ O Pierścień Księżnej Jadwigi ” ( zwykle we wrześniu ) , a także wydarzenie muzyczne pod nazwą Festiwal Jadwigensis ( zwykle w październiku ) .
Jadwiga zmarła 14 lub 15 października 1243 roku w Trzebnicy w opinii świętości i została pochowana w kościele w Trzebnicy .
W marcu 2020 , w czasie renowacji 340-letniego sarkofagu świętej , w jego wnętrzu odkryto mały relikwiarz z jej szczątkami .
Święta Jadwiga .
Kult Jadwigi rozpoczął się zaraz po jej śmierci .
Po odwiedzinach klasztoru w Trzebnicy w 1260 roku legata papieskiego Anzelma rozpoczął się proces kanonizacyjny , popierany przez papieża Urbana IV , który poznał Jadwigę jeszcze jako legat w Polsce .
26 marca 1267 roku papież Klemens IV w kościele dominikanów w Viterbo dokonał kanonizacji Jadwigi , którą zaczęto czcić jako patronkę Polski i całego Śląska .
Kult świętej propagował przez cały okres panowania książę Ludwik I , za którego panowania wykonano tzw .
„ Kodeks lubiński ” z ilustrowaną legendą o św .
Jadwidze Śląskiej .
Grób świętej Jadwigi odwiedzali monarchowie i ich żony : Władysław Łokietek , król węgierski Maciej Korwin ( 1469 ) , żona późniejszego króla Stanisława Leszczyńskiego Katarzyna Opalińska ( 1703 ) .
Propagatorem kultu świętej na Górnym Śląsku był dominikanin Peregryn z Opola , który jest autorem zbioru kazań jej poświęconych .
W 1680 , na prośbę Jana III Sobieskiego i jego żony papież Innocenty XI rozszerzył kult Jadwigi Śląskiej na cały Kościół katolicki .
Po tym wydarzeniu zaczęto czcić świętą na innych ziemiach polskich .
W roku 1746 król Prus Fryderyk II Wielki ufundował katedrę berlińską św .
Jadwigi .
Z okazji jej poświęcenia w 1773 roku kapituła kolegiaty Św .
Krzyża we Wrocławiu jako dar przekazała XVI-wieczny relikwiarz św .
Jadwigi , przechowywany do dziś w katedralnym skarbcu .
W 1929 roku arcybiskup wrocławski kard .
Adolf Bertram podarował klasztorowi w Andechs jako relikwię drzazgę z głowy świętej Jadwigi .
Poza Polską i Niemcami , kult świętej istnieje również w Austrii , Czechach oraz na Węgrzech .
Patronat .
Św .
Jadwiga uznawana jest za patronkę Polski i całego historycznego Śląska , a także małżeństw i chrześcijańskich rodzin .
W XX wieku czczona jako patronka pojednania polsko-niemieckiego .
Województwo dolnośląskie ogłosiło ją swoją patronką , a jej wspomnienie liturgiczne ( 16 października ) od 2010 roku jest obchodzone jako Święto Województwa Dolnośląskiego .
W Polsce patronuje min .
Krosnu Odrzańskiemu , Trzebnicy i Dębicy .
Legenda .
Według podań Jadwiga była osobą posiadającą cechę wielkiej skromności , a jednocześnie bardzo zaangażowaną w swoje działanie .
Cechy te ilustruje legenda , według której Jadwiga , aby nie odróżniać się od reszty swego ludu oraz w imię pokory i skromności , chodziła boso .
Irytowało to bardzo jej męża , wymógł więc na spowiedniku , aby ten nakazał jej noszenie butów .
Duchowny podarował swej penitentce parę butów i poprosił , aby zawsze je nosiła .
Księżna , będąc posłuszną swojemu spowiednikowi , podarowane buty nosiła ze sobą , ale przywieszone na sznurku .
Atrybuty .
Święta Jadwiga przedstawiana jest boso w habicie cysterskim lub płaszczu książęcym , z mitrą książęcą na głowie .
W ręku trzyma buty , makietę kościoła lub klasztoru .
Fundacje świętej .
Tradycja przypisuje księżnej Jadwidze 20 fundacji kościelnych .
Ich budową zajmowali się sprowadzeni z Nadrenii budowniczowie i rzeźbiarze .
