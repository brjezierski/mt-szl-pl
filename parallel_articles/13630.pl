Świniarsko
Świniarsko – wieś w Polsce położona w województwie małopolskim , w powiecie nowosądeckim , w gminie Chełmiec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa nowosądeckiego .
30 czerwca 2004 r. wieś liczyła 1639 mieszkańców .
Do końca 2009 częścią Świniarska była Mała Wieś , obecnie osobna miejscowość .
