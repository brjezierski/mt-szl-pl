Świebodzin ( stacja kolejowa )
Świebodzin ( niem .
`` Schwiebus '' ) – stacja kolejowa w Świebodzinie , w gminie Świebodzin , w województwie lubuskim .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Jest stacją na linii kolejowej nr 3 – trasie pociągów EuroCity : Warszawa Wschodnia – Berlin .
W roku 2017 stacja obsługiwała 500–699 pasażerów na dobę .
