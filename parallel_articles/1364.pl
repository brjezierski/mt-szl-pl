Asikkala
Asikkala – gmina w Finlandii , w regionie Päijät-Häme , z siedzibą w mieście Vääksy .
Populacja wynosi 8,561 - stan na 31 stycznia 2011 .
Powierzchnia wynosi 755.54 km² , z czego 192.13 km² to woda .
Gęstość zaludnienia - 15.19 osób na km² .
Językiem urzędowym gminy , używanym przez 98.6 % mieszkańców , jest fiński .
Na terenie gminy , w pobliżu miasta Lahti , znajduje się Muzeum Lotnictwa Regionu Päijänne Tavastia .
