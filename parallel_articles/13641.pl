Ostrowiec Świętokrzyski ( stacja kolejowa )
Ostrowiec Świętokrzyski – stacja kolejowa w Ostrowcu Świętokrzyskim , w województwie świętokrzyskim , w Polsce .
W roku 2017 stacja obsługiwała 500–699 pasażerów na dobę .
Budynek dworca powstał w 1885 r. Uroczyste otwarcie linii kolejowej od Ostrowca miało miejsce 15 stycznia 1885 .
Była to wówczas stacja końcowa odnogi Kolei Iwanogrodzko-Dąbrowskiej .
W późniejszym okresie linia została przedłużona do Bodzechowa , a w 1915 do Sandomierza .
Wokół stacji kolejowej wyrosło osiedle , które w 1907 r. miało 16 domów i było zamieszkiwane przez 249 osób .
Remonty .
Remont peronu oraz kładki dla pieszych .
