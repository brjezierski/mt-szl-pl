Świątniki ( Wrocław )
Świątniki ( niem .
`` Schwentnig '' ) – część Wrocławia we wschodniej części miasta , na lewym brzegu Oławy .
Wchodzi w skład osiedla Księże i dawnej dzielnicy Krzyki .
Niegdyś wieś służebna , obecnie stanowi teren wrocławskiego przedsiębiorstwa wodociągowgo , zaliczany do dzielnicy Krzyki .
Od sąsiadujących ze Świątnikami od zachodu i południa osiedli Księże Małe i Księże Wielkie oddziela Świątniki lewy dopływ Oławy – potok Zielona .
Na północy , za Oławą , znajdują się Bierdzany i Nowy Dom , na wschodzie znajdują się z grunty podwrocławskich wsi Trestno i Radwanice .
Nazwa .
Miejscowość po raz pierwszy zanotowana w łacińskim dokumencie z 1148 roku jako `` Suecina '' .
