Siemiatycze ( stacja kolejowa )
Siemiatycze – stacja kolejowa w Siemiatyczach-Stacji , w województwie podlaskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2017 stacja obsługiwała 50–99 pasażerów na dobę .
W 1956 roku przy stacji został wybudowany dwukondygnacyjny dworzec .
12 kwietnia 2019 PKP podpisały z przedsiębiorstwem SKB umowę na jego przebudowę .
