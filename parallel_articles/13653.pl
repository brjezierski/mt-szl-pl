Fiołek ogrodowy
Fiołek ogrodowy , bratek ogrodowy , bratek ( `` Viola '' × '' wittrockiana '' Gams ) – gatunek rośliny dwuletniej z rodziny fiołkowatych ( `` Violaceae '' Batsch ) .
Mieszaniec ; powstał w wyniku skrzyżowania trzech dzikich gatunków fiołka : fiołka trójbarwnego ( `` V. tricolor '' ) , fiołka żółtego ( `` V. lutea '' ) i fiołka ałtajskiego ( `` V. altaica '' ) .
W Polsce bardzo często jest uprawiany jako roślina ozdobna ; czasami przejściowo dziczeje ( efemerofit ) .
Uprawa .
Nasiona wysiewa się w końcu czerwca lub na początku lipca , następnie pikuje do rozsadnika .
Na stałe miejsce wysadza się na początku września lub na wiosnę następnego roku .
Sadzonki są w dużym stopniu mrozoodporne , ale dobrze jest okryć je na zimę gałązkami drzew iglastych .
Roślina nie ma specjalnych wymagań co do gleby .
Najlepiej rośnie na stanowisku słonecznym lub półcienistym .
Kwitnie bardzo długo – od kwietnia do końca czerwca .
Po przekwitnięciu należy ją usunąć .
Symbolika .
W tzw .
języku kwiatów bratek symbolizuje troskę i myślenie o kimś .
Wykorzystany jest także jako symbol wolnomyślicielstwa .
