Śnieg
Śnieg – opad atmosferyczny ( hydrometeor ) w postaci kryształków lodu o kształtach głównie sześcioramiennych gwiazdek , łączących się w płatki śniegu .
Po opadnięciu na ziemię tworzy porowatą `` pokrywę śnieżną '' , także nazywaną śniegiem .
Śnieg powstaje , gdy w chmurach para wodna krystalizuje ( resublimuje ) , tworząc kryształy lodu .
Płatek śniegu to struktura kryształów śniegu , mogąca mieć do kilku centymetrów .
Czasem określa się tak samo również pojedyncze kryształki .
Proces powstawania śniegu .
Większość kryształków śniegu jest płaska i ma po sześć , w przybliżeniu identycznych , ramion .
W zależności od temperatury , wilgotności i ciśnienia powietrza , powstają jednak również inne formy , takie jak kolumny , igły , płytki i grudki .
Struktura ich wynika z procesu powstawania , który zaczyna się od kondensacji lodu na cząstce pyłu .
Początkowo powstaje sześciokątny płaski kryształ o wielkości ułamka milimetra .
W temperaturach między –1 a –5 °C oraz między –10 a –20 °C warunki bardziej sprzyjają osadzaniu się lodu na krawędziach i na krysztale wyrasta sześć ramion .
