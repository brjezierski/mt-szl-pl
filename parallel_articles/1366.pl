Askim ( stacja kolejowa )
Askim – kolejowy przystanek osobowy w Askim , w regionie Østfold w Norwegii , jest oddalony od Oslo Sentralstasjon o 53,41 km i 29,10 km od Ski .
Jest położony na wysokości 130,1 m n.p.m .
Ruch pasażerski .
Należy do linii Østfoldbanens østre linje .
Jest elementem - kolei aglomeracyjnej w Oslo - w systemie SKM ma numer 560 .
