Šahy
Šahy ( węg .
`` Ipolyság '' , niem .
`` Eipelschlag '' ) – przygraniczne miasto w południowej Słowacji , w powiecie Levice w kraju nitrzańskim ( region Hont ) .
Miasto leży na wysokości 130 m n.p.m .
nad rzeką Ipolą , u wylotu Kotliny Ipelskiej na Nizinę Naddunajską .
Šahy liczyły 7624 mieszkańców w 2011 roku , w tym około 58 % stanowili Węgrzy , około 39 % Słowacy , około 0,5 % Cyganie , a 0,4 % Czesi .
Powierzchnia miasta wynosi 42,727 km² .
Oprócz historycznych Šahów , stanowiących centrum miasta , w jego skład wchodzą samodzielne niegdyś wsie `` Preseľany nad Ipľom '' , `` Tešmak '' i `` Piesky '' .
Pierwsza z nich jest jedynym należącym do Słowacji osiedlem na południe od rzeki Ipola .
Nazwa miasta po raz pierwszy została zapisana w 1237 jako `` Saag '' i pochodzi prawdopodobnie od starowęgierskiego `` ság '' – „ wzgórze ” .
Przez miasto przebiega międzynarodowa droga E77 Kraków - Banská Bystrica - Budapeszt .
Do 2007 roku znajdowało się tu drogowe przejście graniczne Šahy-Parassapuszta .
Od 1886 Šahy stanowią ważny węzeł kolejowy na trasie z Bratysławy na wschodnią Słowację .
Na odcinku od wsi Kubáňovo do Šahów linia ta biegnie południowym brzegiem Ipoli .
Ze względu na to traktat trianoński przyznał Czechosłowacji obszar na południowym brzegu Ipeli między tymi miastami ( m.in .
dzisiejszą dzielnicę Preseľany nad Ipľom ) .
W mieście rozwinął się przemysł włókienniczy , mięsny oraz maszynowy .
