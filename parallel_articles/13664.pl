Szaki ( wodospad )
Szaki ( ) – wodospad w południowej części Armenii .
Ma wysokość 18 m , co czyni go trzecim najwyższym tego typu obiektem kraju po wodospadach Kasagh i Dżermuk .
Rzeką zasilającą jest Szaki .
Znajduje się w pobliżu wsi Szaki , około 3 km na północny zachód od wypoczynkowego miasta Sisjan w prowincji Sjunik .
Wodospad tworzy głęboki wąwóz , na zboczach którego są jaskinie i nisze skalne .
Położony jest na obszarze chronionym .
W pobliżu znajdują się pozostałości osadnictwa z paleolitu .
Wodospad cieszył się popularnością wśród turystów do lat 90. , kiedy to w pobliżu zbudowano małą elektrownię wodną , do której przekierowano część wody zasilającej wodospad .
W 2017 rząd Armenii zadecydował , że przynajmniej w sezonie turystycznym przez większość dnia woda będzie w pełni przepływać przez wodospad .
Obecnie znów jest atrakcją turystyczną i odwiedzany także przez turystów spoza Armenii .
Do wodospadu prowadzi szlak turystyczny o długości 1,8 km rozpoczynający się przy drodze krajowej H44 .
Maksymalna ilość wody przepływa przez wodospad w godzinach 11:00–19:00 pomiędzy 1 maja a 30 września oraz od 11:00 do 17:00 pomiędzy 15 marca a 30 kwietnia i 1 października a 15 listopada .
