Szybenik
Szybenik ( , , , ) – miasto w Chorwacji , stolica żupanii szybenicko-knińskiej , siedziba miasta Szybenik .
Jest położone w środkowej Dalmacji , nad ujściem rzeki Krka do Morza Adriatyckiego .
W 2021 roku liczyło 31 115 mieszkańców .
Szybenik jest politycznym , kulturalnym , komunikacyjnym i turystycznym centrum swojego regionu .
Historia .
Po raz pierwszy nazwa Szybenik pojawia się w 1066 w statucie króla chorwackiego Petara Krešimira IV .
W przeciwieństwie do innych miast w Dalmacji założonych przez Ilirów , Greków czy Rzymian jest najstarszym miastem rodowym Chorwatów na wschodnim wybrzeżu Adriatyku .
Szybenik otrzymał prawa miejskie w 1298 , wtedy też w mieście erygowano diecezję .
