Šime Vrsaljko
Šime Vrsaljko ( ur .
10 stycznia 1992 w Zadarze ) – chorwacki piłkarz występujący na pozycji obrońcy .
Jest synem Mladena Vrsaljko , chorwackiego środkowego obrońcy , który większość kariery spędził w NK Zadar .
Kariera klubowa .
Karierę piłkarską Šime Vrsaljko rozpoczął w Dinamie Zagrzeb .
Grał w nim w drużynie juniorów , ale nim trafił do seniorów , został w 2009 roku wypożyczony do innego klubu z Zagrzebia , NK Lokomotiva .
26 lipca 2009 roku zadebiutował w jego barwach w pierwszej lidze chorwackiej w przegranym 0:6 wyjazdowym meczu z HNK Rijeka .
W rundzie jesiennej sezonu 2009/2010 był podstawowym zawodnikiem Lokomotivy i rozegrał w niej wszystkie 17 meczów .
W grudniu 2009 Vrsaljko wrócił do Dinama by grać w tym klubie w rundzie wiosennej sezonu 2009/2010 .
Swoje pierwsze spotkanie ligowe w Dinamie Vrsaljko rozegrał 27 lutego 2010 z Croatią Sesvete , wygrane przez Dinamo 6:0 .
W Dinamie , podobnie jak w Lokomotivie , także stał się podstawowym zawodnikiem .
