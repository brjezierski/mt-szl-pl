Šiprage ( cyr .
Шипраге ) – wieś w Bośni i Hercegowinie , w Republice Serbskiej , w gminie Kotor Varoš .
W 2013 roku liczyła 652 mieszkańców .
Podczas wojny w Bośni ( 1992-95 ) jeden z 18 bacnikich obozów koncentracyjnych w Kotor Varoš był na posterunku policji ( MUP ) Šiprage
Klimat .
Šiplage i okolice mają umiarkowany klimat kontynentalny , wyróżnia się cztery pory roku : wiosnę , lato , jesień i zimę .
Z powodu górskiego krajobrazu wahania temperatury i opady są stosunkowo wysokie .
