Szumsk ( miasto )
Szumsk ( ukr .
Шумськ ) – miasto na Ukrainie , w obwodzie tarnopolskim w rejonie krzemienieckim , była siedziba rejonu szumskiego , do 1945 w Polsce , w województwie wołyńskim , w powiecie krzemienieckim , siedziba gminy Szumsk .
Historia .
Szumsk jest jedną z najwcześniej wzmiankowanych miejscowości na Wołyniu ( 1149 ) .
W 1637 roku założono tu klasztor bazylianów , zniszczony wraz z całym miastem podczas powstania Chmielnickiego .
