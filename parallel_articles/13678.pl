Nowe Święciany ( stacja kolejowa )
Nowe Święciany ( , ) – węzłowa stacja kolejowa w miejscowości Nowe Święciany , w rejonie święciańskim , na Litwie .
Leży na linii dawnej Kolei Warszawsko-Petersburskiej .
Stacja powstała w XIX w. pomiędzy stacjami Podbrodzie i Ignalin .
Dawniej od stacji odchodziła linia wąskotorowa Nowe Święciany – Łyntupy przez Święciany do stacji Łyntupy .
Po II wojnie światowej ze stacji Nowe Święciany poprowadzono ślepą linię do Uciany , przebiegającą po trasie wcześniejszej linii wąskotorowej .
