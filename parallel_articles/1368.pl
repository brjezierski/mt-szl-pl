Asmara ( stacja kolejowa )
Asmara – stacja kolejowa obsługująca ruch pasażerski w stolicy Erytrei , Asmarze .
Dworzec został otwarty 5 grudnia 1911 wraz z ukończeniem ostatniego odcinka linii kolejowej z Massauy .
Od 1923 rozpoczęły się ruch pasażerski na linii kolejowej do Keren , która została otwarta częściowo już w 1914 dla transportu towarowego .
