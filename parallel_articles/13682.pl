Uzbekistan
Uzbekistan ( uzb .
`` Oʻzbekiston '' , Republika Uzbekistanu – `` Oʻzbekiston Respublikasi '' ) – państwo położone w Azji Środkowej , członek Wspólnoty Niepodległych Państw .
Jest krajem podwójnie śródlądowym .
Geografia .
Uzbekistan leży w Azji Środkowej między dwiema wielkimi rzekami – Amu-darią i Syr-darią .
Powierzchnia w większości równinna .
Około 20 % powierzchni kraju stanowią góry i przedgórza .
Na wschodzie i północnym wschodzie kraju leżą Góry Ałajskie i Tienszan , gdzie znajduje się najwyższy szczyt kraju – Hazrat Sulton , osiągający wysokość 4634 m n.p.m .
Między nimi leży urodzajna , nawadniana Kotlina Fergańska , której część należy do Uzbekistanu .
Środkową część kraju zajmuje Nizina Turańska z rozległą pustynią Kyzył-kum , na której terenami osadniczymi są nieliczne oazy i doliny rzek Syr-darii , Zarafszanu , Kaszka-darii i Amu-darii .
Zachodni skraj zajmuje równie pustynny płaskowyż Ustiurt .
Do Uzbekistanu należy południowa część dawnego Jeziora Aralskiego .
Klimat kontynentalny skrajnie suchy , z ciepłymi zimami ( temperatura wahająca się w przedziale 3 do 10 °C ) i gorącym , suchym latem , z temperaturami sięgającymi nawet 45 °C .
Mała ilość opadów ( do 200 mm na terenach równinnych ) , nie sprzyja rozwojowi rolnictwa , i czyni koniecznym nawadnianie pól uprawnych .
Uzbekistan jest krajem podwójnie śródlądowym .
Łączna długość granic z poszczególnymi sąsiadami wynosi 6221 km ( Afganistan 137 km , Kazachstan 2203 km , Kirgistan 1099 km , Tadżykistan 1161 km , Turkmenistan 1621 km ) .
Do największych miast należą : Taszkent ( stolica ) , Samarkanda , Namangan , Andiżan , Buchara , Fergana , Chiwa .
Historia .
