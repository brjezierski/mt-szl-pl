W VIII wieku Uzbekistan dostał się pod panowanie Arabów .
Ich najazd doprowadził do upadku rozwijającej się od czasów Aleksandra kultury , która , odmieniona za przyczyną głębokiej islamizacji , mogła odrodzić się dopiero w IX w. W X wieku nastąpiło opuszczenie ziem przez wojska arabskie i objęcie rządów przez irańską dynastię Samanidów .
W latach 1220–1221 nastąpił tragiczny w skutkach najazd wojsk Czyngis-chana , który pozostawił po sobie wiele zniszczeń .
Dopiero w XIV wieku Mongołowie zostali wyparci przez Timura , a terytorium obecnego Uzbekistanu wkroczyło na drogę szybkiego rozwoju .
