Zdzieszowice ( stacja kolejowa )
Zdzieszowice – stacja kolejowa w Zdzieszowicach , w województwie opolskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W maju 1846 r. , na wniosek leśnickiego magistratu , dyrekcja kolei wybudowała drewnianą poczekalnię w Zdzieszowicach .
Dwa lata później , ze względu na niewielką liczbę pasażerów stację zdemontowano .
W roku 1860 , została w tym miejscu wybudowana , nowoczesna , jak na owe czasy , stacja kolejowa „ Bahnhof Leschnitz ” .
Forma architektoniczna stacji nie uległa zmianie do dnia dzisiejszego .
W roku 2018 stacja obsługiwała 1000–1500 pasażerów na dobę .
