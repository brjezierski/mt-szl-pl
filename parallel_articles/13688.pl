Zimne Kąty
Zimne Kąty ( , ) – część wsi Rudziczka w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Przepływa przez nią potok Meszna .
W latach 1975–1998 część wsi położona była w województwie opolskim .
Historia .
Na terenie Zimnych Kątów znajdował się zamek , zburzony w czasach PRL-u .
Po wojnach pruskich i napoleońskich miasto Prudnik było zadłużone .
Z tego powodu w 1819 sprzedało Zimne Katy kupcowi Höhlmannowi ze Świdnicy .
Według `` Meyers Gazetteer '' Zimne Kąty były zamieszkiwane przez 36 osób .
15 grudnia 1949 r. nadano miejscowości , wówczas administracyjnie związanej z Rudziczką , polską nazwę `` Zimne Kąty '' .
W 2002 odbudowana została droga łącząca Zimne Kąty z Rudziczką .
