Asnières-sur-Seine
Asnières-sur-Seine – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Do 1968 `` Asnières '' , od średniowiecznego łacińskiego Asnerias – hodowla osłów .
`` sur-Seine '' dodano dla odróżnienia od innych gmin noszących nazwę `` Asnières '' .
Według danych na rok 2012 gminę zamieszkiwało 83 845 osób , a gęstość zaludnienia wynosiła 17 111 osób/km² .
Wśród 1287 gmin regionu Île-de-France Asnières-sur-Seine plasuje się na 690. miejscu pod względem powierzchni .
