Ziemia ( podłoże )
Ziemia – jednorodne lub skomponowane z wielu składników podłoże , przygotowane specjalnie dla określonych upraw ogrodniczych , np .
ziemia kompostowa , ziemia torfowa , ziemia liściowa .
W produkcji szklarniowej występuje ziemia szklarniowa , tj .
utwór powstały z wierzchniej warstwy skorupy ziemskiej , odizolowany od czynników pogodowych i uzupełniony znacznymi dodatkami często niekonwencjonalnych materiałów organicznych i mineralnych .
W ziemi szklarniowej zachowany jest stały kontakt z warstwami i poziomami genetycznymi znajdującymi się poniżej silnie zmienionych poziomów próchnicznych .
Są to ziemie z warstw uprawnych pól , łąk , pastwisk i ugorów ( ziemia polowa , ogrodowa i darniowa ) .
