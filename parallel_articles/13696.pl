Ziemlin
Ziemlin – wieś w Polsce położona w województwie wielkopolskim , w powiecie gostyńskim , w gminie Krobia .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość należała do wsi większych w ówczesnym pruskim powiecie Kröben ( krobskim ) w rejencji poznańskiej .
Ziemlin należał do okręgu krobskiego tego powiatu i stanowił odrębny majątek , którego właścicielem był wówczas ( 1846 ) Leopold Unruh .
Według spisu urzędowego z 1837 roku wieś liczyła 192 mieszkańców , którzy zamieszkiwali 25 dymów ( domostw ) .
W latach 1975−1998 miejscowość administracyjnie należała do województwa leszczyńskiego .
