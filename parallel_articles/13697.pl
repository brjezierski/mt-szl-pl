Ziomaki ( powiat szydłowiecki )
Ziomaki – wieś w Polsce położona w województwie mazowieckim , w powiecie szydłowieckim , w gminie Szydłowiec .
Prywatna wieś szlachecka `` Ziemaki '' , położona była w drugiej połowie XVI wieku w powiecie radomskim województwa sandomierskiego .
W latach 1975–1998 miejscowość należała administracyjnie do województwa radomskiego .
