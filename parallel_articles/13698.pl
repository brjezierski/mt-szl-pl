Niezapominajka
Niezapominajka ( `` Myosotis '' L. – dosłownie `` mysie uszko '' z greckiego `` mys '' – mysz ( w dopełniaczu `` myos '' ) i `` us '' – ucho ( w dopełniaczu `` otos '' ) ) – rodzaj roślin należący do rodziny ogórecznikowatych .
Obejmuje ok. 150 gatunków roślin zielnych .
Występują głównie w strefie klimatu umiarkowanego na obu półkulach .
W Polsce dziko rośnie 13 gatunków .
Zasiedlają bardzo różne siedliska : miejsca suche i piaszczyste wydmy , trawiaste murawy , zarośla i lasy , miejsca wilgotne i płytkie wody , a także wysokie piętra górskie .
Niezapominajki są popularnymi roślinami ozdobnymi .
Najczęściej uprawiana jest niezapominajka leśna `` M. sylvatica '' , sadzona w różnych odmianach .
Rozmieszczenie geograficzne .
Niezapominajki spotykane są na wszystkich kontynentach z wyjątkiem Antarktydy i Ameryki Południowej , gdzie rosną jednak gatunki introdukowane .
Najbardziej zróżnicowane są w dwóch oddalonych od siebie obszarach – w basenie Morza Śródziemnego i na Nowej Zelandii .
W Europie , w większości w jej części południowej , rośnie 41 gatunków .
W Turcji rosną 23 gatunki , a w Nowej Zelandii – 33 .
Stosunkowo nieliczne gatunki rosną zwłaszcza w Afryce i na kontynentach amerykańskich .
Rośliny te występują głównie w strefie klimatu umiarkowanego , a w strefie międzyzwrotnikowej ich obecność ograniczona jest to obszarów górskich .
