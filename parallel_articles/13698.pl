Systematyka .
Rodzaj należy do podplemienia `` Myosotideae '' , plemienia `` Rochelieae '' w podrodzinie `` Cynoglossoideae '' w obrębie rodziny ogórecznikowatych `` Boraginaceae '' .
Obecność w kulturze .
Corocznie , od 2002 r. w dniu 15 maja , obchodzone jest w Polsce Święto Polskiej Niezapominajki .
Jego inicjatorem był Andrzej Zalewski , który wystąpił z taką propozycją w programie Ekoradio , emitowanym przez Polskie Radio Program I .
