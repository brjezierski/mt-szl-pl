Żabno ( powiat bytowski )
Żabno ( kaszb .
`` Żabno '' ) – wieś w Polsce położona na Pojezierzu Bytowskim , w województwie pomorskim , w powiecie bytowskim , w gminie Miastko .
Wieś wchodzi w skład sołectwa Role-Żabno .
W latach 1973–1976 miejscowość była siedzibą gminy Żabno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
