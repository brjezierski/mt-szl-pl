Żabokliki-Kolonia
Żabokliki-Kolonia – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 4 km na północny wschód od centrum Siedlec .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Maksymiliana Marii Kolbego w Siedlcach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
