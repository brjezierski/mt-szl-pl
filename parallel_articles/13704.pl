`` Żegań '' , `` Żegan '' lub `` Zegan '' ; , `` Sagan in Schlesien '' lub `` Sagan am Bober '' ; łuż .
`` Zahań '' ; ; , `` Saganum '' , `` Sagnum '' , `` Zagano '' lub `` Zager '' ) – miasto , będące jednocześnie gminą miejską i siedzibą gminy wiejskiej Żagań , zlokalizowane w zachodniej Polsce , w województwie lubuskim , na pograniczu Niziny Śląsko-Łużyckiej i Wału Trzebnickiego , nad Bobrem i Czerną Wielką ; siedziba powiatu żagańskiego .
Usytuowane w odległości 40 km od granicy polsko-niemieckiej i 100 km od granicy polsko-czeskiej .
Według danych GUS z 31 grudnia 2019 r. Żagań liczył 25 668 mieszkańców .
Żagań jest lokalnym ośrodkiem usługowo-przemysłowym , a także węzłem komunikacyjnym , w którym zbiega się droga krajowa nr 12 i dwie drogi wojewódzkie oraz kilka linii kolejowych .
Miasto jest siedzibą dowództwa 11 Lubuskiej Dywizji Kawalerii Pancernej oraz kilku innych jednostek wojskowych .
