Żakowice ( województwo wielkopolskie )
Żakowice – wieś w Polsce położona w województwie wielkopolskim , w powiecie ostrowskim , w gminie Nowe Skalmierzyce , w Kaliskiem , ok. 7 km od Nowych Skalmierzyc , ok. 8 km od Kalisza .
Podział administracyjny .
W XIX w. miejscowość należała administracyjnie do powiatu pleszewskiego .
W latach 1975–1998 do województwa kaliskiego , od 1999 do powiatu ostrowskiego .
Historia .
W źródłach od 1403 roku .
Pod koniec XVI w. były własnością Wojciecha Żakowskiego i Wojciecha Wolińskiego .
W 1618 wieś była w posiadaniu Adama Żakowskiego .
W XIX w. podlegały pod urząd stanu cywilnego w Sobótce .
Według `` Słownika geograficznego Królestwa Polskiego '' z 1895 w miejscowości było 8 domów i 101 mieszkańców .
