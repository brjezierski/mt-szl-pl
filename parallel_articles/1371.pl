Aspergillus candidus
Aspergillus candidus ( `` Aspergillus candidus '' Link ) – gatunek grzybów z rzędu kropidlakowców ( `` Eurotiales '' ) .
Gatunek kosmopolityczny , występujący na całym świecie , głównie w ciepłych rejonach .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy zdiagnozowany został w 1809 r. przez Linka i według Index Fungorum nazwa podana przez tego autora jest prawidłowa .
Później przez różnych autorów opisywany był pod różnymi nazwami i zaliczany do różnych rodzajów , wyróżniono też w jego obrębie różne podgatunki , odmiany i formy .
Według Index Fungorum obecnie wszystkie one są synonimami `` Aspergillus candidus ''
Morfologia .
Na podłożu Czapeka ( CzA ) rośnie wolno , na MEA szybciej , na CREA słabo , wzrastają kolonie płaskie o powietrznej strukturze , przechodzące stopniowo od koloru białego ( na MEA białe ) do kremowego i żółtokremowego , czasem sklerocja przechodzą z białych w purpurowe a nawet czarne ; spód kolonii jest bezbarwny lub bardzo jasnożółty do słabo różowego , u niektórych szczepów występuje czarnopurpurowa obwódka .
Główki konidioforów mają promienisty układ zarodników .
Metule mają 7–20 × 5–8 μm długości , fialidy 6–9 × 2–3 μm .
Zarodniki są okrągłe do elipsoidalnych , gładkie z cienkimi ścianami o średnicy 2,5 × 4,0 μm .
Występowanie .
Występuje na całym świecie , szczególnie w klimacie równikowym i podzwrotnikowym w glebach , na nasionach i ryzosferze , nigdy w glebie bez obecności korzeni .
Znajdowany w żywności i paszach zwierzęcych , głównie zbożach .
