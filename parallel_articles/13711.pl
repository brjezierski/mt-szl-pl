Żary ( rejon dokszycki )
Żary ( ; ) – wieś na Białorusi , w obwodzie witebskim , w rejonie dokszyckim , w sielsowiecie Krypule .
Historia .
W czasach zaborów w powiecie wilejskim w guberni wileńskiej Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym wieś i folwark leżały w Polsce , w województwie wileńskim , w powiecie duniłowickim , od 1926 w powiecie dziśnieńskim , w gminie Parafianów .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało :
Wierni należeli do parafii rzymskokatolickiej w Dokszycach i prawosławnej w Hnieździłowie .
Miejscowość podlegała pod Sąd Grodzki w Dokszycach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Parafianowie .
Po agresji ZSRR na Polskę w 1939 roku wieś znalazła się w granicach BSRR .
W latach 1941–1944 była pod okupacją niemiecką .
Następnie leżała w BSRR .
Od 1991 roku w Republice Białorusi .
