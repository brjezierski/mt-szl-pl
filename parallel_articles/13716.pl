Żeglce
Żeglce – wieś w Polsce położona w województwie podkarpackim , w powiecie krośnieńskim , w gminie Chorkówka .
Miejscowość jest siedzibą rzymskokatolickiej parafii Matki Bożej Częstochowskiej należącej do dekanatu Krosno III w archidiecezji przemyskiej .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krośnieńskiego .
Historia Żeglec .
W XI wieku Żeglce należały do wsi szlacheckich na terenie przylegającym do Krosna .
