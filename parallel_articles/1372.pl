Kropidlak żółty
Kropidlak żółty ( `` Aspergillus flavus '' Link ) – gatunek grzybów z rodziny `` Aspergillaceae '' .
Gatunek kosmopolityczny , występujący na całym świecie , głównie w rejonach tropikalnych .
Jest organizmem modelowym w badaniach naukowych .
Systematyka i nazewnictwo .
Takson ten po raz pierwszy zdiagnozowany został w 1809 r. przez Linka i według Index Fungorum nazwa podana przez tego autora jest prawidłowa .
Później przez różnych autorów opisywany był pod różnymi nazwami i zaliczany do różnych rodzajów , wyróżniono też w jego obrębie różne odmiany .
Według Index Fungorum obecnie wszystkie one są synonimami `` Aspergillus flavus '' .
