Morfologia .
Na podłożu Czapka-Doxa wzrastają kolonie filcowate koloru żółtego , które szybko ciemnieją do koloru żółtozielonego , a następnie do zielonego ; spód kolonii jest bezbarwny .
Główki konidioforów mają promienisty układ zarodników .
Metule mają 6–10 × 5 μm długości , fialidy 6–10 × 3–5 μm .
Struktury zarodnikotwórcze mają układ dwurzędowy i pokrywają całą główkę .
Zarodniki są zielonawe , okrągłe i szorstkie .
