Żeleznodorożnyj ( obwód moskiewski )
Żeleznodorożnyj ( ros .
`` Железнодорожный '' ) – część miasta Bałaszycha w Rosji , w obwodzie moskiewskim położona 21 km na wschód od Moskwy .
Do 2015 roku samodzielne miasto .
Już w XVI wieku istniało na terenie obecnego miasta kilka wsi , lecz powstanie miasta możliwe było dopiero wraz z budową linii kolejowej do Niżnego Nowogrodu w latach 1858-1862 .
W roku 1861 powstała tu stacja kolejowa i w 1877 była tu wieś , a od 1951 miasto .
