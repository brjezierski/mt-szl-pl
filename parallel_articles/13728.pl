Żochy ( powiat sokołowski )
Żochy – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Kosów Lacki .
W latach 1975–1998 miejscowość należała administracyjnie do województwa siedleckiego .
Wierni kościoła rzymskokatolickiego należą do parafii Parafia Narodzenia Najświętszej Maryi Panny w Kosowie Lackim .
