Kropidlak popielaty
Kropidlak popielaty ( `` Aspergillus fumigatus '' Fresenius ) – gatunek grzybów z rzędu kropidlakowców ( `` Eurotiales '' ) .
Gatunek kosmopolityczny , występujący na całym świecie .
Jest organizmem modelowym w badaniach naukowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillace , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy zdiagnozowany został w 1863 r. przez Georga Freseniusa i według Index Fungorum nazwa podana przez tego autora jest prawidłowa .
Później przez różnych autorów opisywany był pod różnymi nazwami i zaliczany do różnych rodzajów , wyróżniono też w jego obrębie różne podgatunki , odmiany i formy .
Według Index Fungorum obecnie wszystkie one są synonimami `` Aspergillus fumigatus '' :
Morfologia .
Na podłożu Czapeka ( CzA ) szybko , na podłożu MEA jeszcze szybciej , na podłożu CREA wolno , wzrastają cienkie aksamitne kolonie o filcowatej strukturze głęboko pofałdowane lub skrajnie kosmate koloru białego , przechodzące stopniowo przez różne tonacje zielonego do niebiesko-zielonej , ciemnozielonej czasem szarej ; spód kolonii jest bezbarwny lub podobnie jak grzybnie w różnych odcieniach zieleni , do ciemnego brązu .
Główki konidioforów mają kolumnowy układ zarodników .
Układ strzępek końcowych jest jednorzędowy obecne są tylko fialidy 5–10 × 2–3 μm .
Zarodniki są okrągłe lub okrągławe , szorstkie oraz zielone o średnicy 2,5 × 3,0 μm .
Występowanie .
Występuje na całym świecie .
Występuje w wyższych temperaturach na gnijących resztkach roślinnych , szczególnie obecny w powietrzu w oczyszczalniach biologicznych .
