Żukowo ( Białoruś )
Żukowo ( ; ) – wieś na Białorusi , w obwodzie witebskim , w rejonie postawskim , w sielsowiecie Wołki .
Historia .
W czasach zaborów w guberni wileńskiej Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym wieś a następnie kolonia leżała w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Norzyca .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 96 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Były tu 22 budynki mieszkalne .
W 1931 w 23 domach zamieszkiwało 110 osób .
Wierni należeli do parafii rzymskokatolickiej w Duniłowiczach i prawosławnej w Norzycy .
Miejscowość podlegała pod Sąd Grodzki w Duniłowiczach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Łasicy .
Po agresji ZSRR na Polskę w 1939 roku wieś znalazła się w granicach BSRR .
W latach 1941–1944 była pod okupacją niemiecką .
Następnie leżała w BSRR .
Od 1991 roku w Republice Białorusi .
