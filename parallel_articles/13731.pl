Wólka Żukowska
Wólka Żukowska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Wierni Kościoła rzymskokatolickiego należą do parafii Wniebowzięcia NMP w Niwiskach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
3 km od rzeki Liwiec .
Teren ciekawy pod względem ornitologicznym .
Możliwość obserwacji czajki zwyczajnej , żurawia zwyczajnego , dudka zwyczajnego , dzierzby gąsiorka , błotniaka stawowego , sąsiadujący z obszarem Natura 2000 , Doliną Liwca terenem objętym specjalną ochroną ptaków ( Dyrektywa Ptasia ) .
