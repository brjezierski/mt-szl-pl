Żuromin
Żuromin – miasto w Polsce położone w północno-zachodniej części województwa mazowieckiego , leżące na Równinie Raciąskiej .
Pełni funkcję ośrodka administracyjnego i gospodarczego .
Jest siedzibą powiatu obejmującego 6 gmin oraz siedzibą gminy miejsko-wiejskiej Żuromin .
W latach 1975–1998 miasto administracyjnie należało do województwa ciechanowskiego .
Etymologia nazwy .
Etymologię nazwy Żuromin wywodzić można od usytuowania środowiskowego pierwotnej osady , która powstała na jednej z puszczańskich polan , gdzie odpoczywali myśliwi , polujący na zwierzęta futerkowe .
Pierwotna nazwa różniła się nieco od dzisiejszej i brzmiała : Żeremin , Żyromin , Żeromino .
Charakterystyczny jest tu człon `` żer- '' wskazujący na żerowanie zwierząt leśnych , chociaż jest niewykluczone , iż nazwa związana była z żeremiami bobrowymi ( siedliskami bobrów ) , których – z uwagi na znaczną bagnistość środowiska – było na tym terenie bardzo dużo .
