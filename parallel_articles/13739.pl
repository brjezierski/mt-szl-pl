Kalendarz żydowski
Kalendarz żydowski lub kalendarz hebrajski ( hebr .
הלוח העברי `` luach ha-szana '' ) – system rachuby czasu oparty na cyklu księżycowym i słonecznym , w którym rok składa się z dwunastu miesięcy ( hebr .
`` chodaszim '' ) liczących po 29 lub 30 dni , które rozpoczynają się od nowiu księżyca , a różnice w stosunku do kalendarza słonecznego są niwelowane za pomocą dodatkowego , trzynastego miesiąca .
Rok żydowski w ujęciu kalendarza słonecznego liczy 354 dni 8 godzin 48 minut i 45 sekund .
Rok przestępny przypada siedem razy w ciągu dziewiętnastu lat , czyli co dwa lub trzy lata i składa się z trzynastu miesięcy .
Trzynastym jest `` adar szeni '' .
Rok świecki zaczyna się jesienią , a rok liturgiczny – wiosną .
Doba rozpoczyna się i kończy o zachodzie słońca .
Pora dzienna jest dzielona na 12 godzin , więc godzina hebrajska trwa nie 60 minut , ale 1/12 część czasu , jaki upływa pomiędzy wschodem słońca a jego zachodem .
Ma to znaczenie w odniesieniu do porządku modlitewnego , bowiem określone modlitwy winny być odprawiane w konkretnych porach dnia .
Informacje historyczne .
W czasach prahistorycznych rytm bytowania ludów koczowniczych wyznaczało światło naturalne , a mitologia hebrajska koncentrowała się na odniesieniach do sił przyrody , w tym światła księżyca , lecz brak informacji na temat kształtowania się żydowskiego kalendarza .
W okresie starożytnym rozpoczęcie kolejnego miesiąca ( a raczej `` Rosz Chodesz '' – początek odnowie­nia miesiąca ) ogłaszał Sanhedryn , po uprzednim potwierdzeniu wystąpienia nowiu księżyca .
Informację ogłaszali kapłani dmąc w trąby na wieży , a na wzgórzach rozpalane były ogniska , by w ten sposób przekazywać informację na odległość .
Dalej wiadomość zanosili specjalni posłańcy .
Opóźnienie , jakie mogło powstać przy przekazywaniu wiadomości , legło u podstaw ustanowienia zwyczaju dodawania w diasporze jednego dnia świątecznego „ na zapas ” ( hebr .
`` jom tow szeni szel galujot '' = dosł .
: drugi dzień świąteczny dla wygnania ) .
Lata przestępne były wyznaczane i ogłaszane przez radę złożoną z co najmniej trzech rabinów .
Akceptację musiał wyrazić `` nasi '' , który przewodził Sanhedrynowi .
W czasach biblijnych bramy dziedzińca świątynnego były otwierane podczas nowiu , by lud mógł składać ofiary całopalne przepisane przez Prawo .
Podczas nowiu świętowano , ucztowano i powstrzymywano się od pracy .
W czasie nowiu nie wolno było pościć , a kobiety nie śpiewały pieśni żałobnych kinot .
Na przestrzeni wieków nów tracił na znaczeniu jako święto .
Talmud zezwalał już na wykonywanie pracy .
W średniowieczu kobiety żydowskie powstrzymywały się jednak od szycia , a traktat „ Szulchan aruch ” pochwalał ten obyczaj .
Żydówki grywały w tym dniu w karty .
Na stołach pojawiały się lepsze potrawy .
Chasydzi podtrzymywali tradycję wieczornych uczt , w skład których wchodziły ryby , mięsa i piwo .
W 359 roku patriarcha palestyński , `` nasi '' Hillel II przeprowadził reformę kalendarza , która doprowadziła do uzgodnienia roku lunarnego z cyklem solarnym .
U podstaw reformy leżały precyzyjne obliczenia , które pozwalały na rezygnację z konieczności prowadzenia obserwacji księżyca dla wyznaczenia początku nowego miesiąca .
Ustała już także konieczność uzależniania dat świątecznych od decyzji Sanhedrynu i dodawania do kalendarza dni i miesięcy dodatkowych .
Od tego czasu Żydzi żyjący w diasporze mogli samodzielnie ustalać daty świąt .
Reforma Hillela II wyznaczyła obowiązujący standard żydowskiego kalendarza , który w niezmienionej formie funkcjonuje już od ponad 1600 lat .
Rachuba lat .
W kalendarzu żydowskim rachuba lat zaczyna się od dnia stworzenia świata , które wg ustaleń żydowskich autorytetów religijnych nastąpiło 7 października 3761 p.n.e. , stąd np .
w roku trwa żydowski rok / .
Rok w kalendarzu żydowskim składa się z dwunastu miesięcy ( hebr .
`` chodaszim '' ) liczących po 29 lub 30 dni , które rozpoczynają się od nowiu księżyca , a różnice w stosunku do kalendarza słonecznego są niwelowane za pomocą dodatkowego , trzynastego miesiąca .
Rok żydowski ma 353 , 354 lub 355 dni .
Rok księżycowy trwa 354 dni 8 godzin 48 minut i 45 sekund .
Rok przestępny przypada siedem razy na przestrzeni dziewiętnastu lat , czyli co dwa lub trzy lata , wydłuża cykl o 30 dni i składa się z trzynastu miesięcy .
Trzynastym jest `` adar szeni '' .
Rok świecki zaczyna się jesienią , a rok liturgiczny – wiosną .
Miesiące .
Rok hebrajski dzieli się na 12 miesięcy liczących 29 lub 30 dni , ponadto co trzy , rzadziej co dwa lata , dla zrównania cyklu solarnego z lunarnym dodawany jest dodatkowy , trzynasty miesiąc – zwany `` adar szeni '' .
Drugi adar dodawany jest w ramach 19-letniego okresu ( cykl Metona ) , obejmującego następujące po sobie lata , a dodawany jest zawsze do 3 , 6 , 8 , 11 , 14 , 17 i 19 roku tego okresu .
Poszczególne miesiące rozpoczynają się od nowiu księżycowego ( Rosz chodesz ) .
Tydzień .
W kalendarzu hebrajskim nazwy poszczególnych dni pochodzą wprost od liczb porządkowych ( np .
niedziela – dzień pierwszy , czyli `` jom riszon '' , itd ) , ale ostatni , siódmy nosi nazwę `` szabat '' , która oznacza „ odpoczynek ” .
Zgodnie z tradycją biblijną szabat jest dniem świątecznym przeznaczonym na odpoczynek .
Doba .
Zgodnie z tradycją żydowską doba rozpoczyna się i kończy o zachodzie słońca .
Pora dzienna jest dzielona na 12 godzin .
W konsekwencji godzina hebrajska trwa nie 60 minut , ale 1/12 część czasu , jaki upływa pomiędzy wschodem słońca a jego zachodem .
Ma to znaczenie w odniesieniu do porządku modlitewnego , bowiem określone modlitwy winny być odprawiane w konkretnych porach dnia .
Konsekwentnie – połowa nocy ( północ ) nie występuje o godzinie 0.00 , ale w połowie czasu między zachodem a wschodem słońca .
Podobnie południe w hebrajskiej rachubie godzin nie występuje o godzinie 12.00 , lecz w połowie czasu , jaki upływa między wschodem a zachodem słońca .
