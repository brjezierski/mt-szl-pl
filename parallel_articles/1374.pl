Aspergillus nidulans
Aspergillus nidulans ( `` Aspergillus nidulans '' ( Eidam ) G. Winter ) – gatunek grzybów z rzędu kropidlakowców ( `` Aspergilales '' ) .
Gatunek kosmopolityczny , występujący na całym świecie , głównie w ciepłych rejonach .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergilaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy opisany został w 1883 r. jako `` Sterigmatocystis nidulans '' przez Eidama , w 1884 r. został zakwalifikowany przez H.G .
Wintera do rodzaju `` Aspergillus '' i według Index Fungorum nazwa podana przez tego autora jest prawidłowa .
Później przez różnych autorów opisywany był pod różnymi nazwami i zaliczany do różnych rodzajów , Wyróżniono też w jego obrębie różne podgatunki , odmiany i formy .
Według Index Fungorum obecnie wszystkie one są synonimami `` Aspergillus nidulans '' .
Morfologia .
Na podłożu Czapeka ( CzA ) wolno , na MEA szybciej , wzrastają płaskie , koloru ciemnozielonego albo miodowożółtego ; spód kolonii jest ciemnopurpurowy .
Główki konidioforów mają kolumnowy układ zarodników .
Metule mają 5–6 × 2–3 μm długości , fialidy 5–9 × 2–3 μm .
Zarodniki są okrągłe , pomarszczone , półprzeźroczyste , w skupiskach zielone , o średnicy 3,0 × 4.0 μm .
Występowanie .
Występuje na całym świecie , szczególnie na rozkładających się szczątkach roślinnych .
