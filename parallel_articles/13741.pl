Żydówka z cytrynami
Żydówka z cytrynami ( znany też pod tytułem Pomarańczarka ) – obraz olejny polskiego malarza Aleksandra Gierymskiego z 1881 roku , znajdujący się w zbiorach Muzeum Śląskiego w Katowicach .
Obraz powstał w czasie pobytu Gierymskiego w Warszawie , w latach 1876–1888 .
Artysta związał się w tym czasie z grupą młodych literatów i krytyków , zgromadzonych wokół pozytywistycznego czasopisma „ Wędrowiec ” i podjął współpracę z Stanisławem Witkiewiczem oraz Antonim Sygietyńskim .
Efektem tej współpracy był cykl realistycznych scen rodzajowych , przedstawiających życie żydowskich mieszkańców Powiśla i innych ubogich dzielnic Warszawy .
Powstały w tym czasie takie prace , jak `` Żydówka sprzedająca pomarańcze '' , `` Żydówka sprzedająca cytryny '' , `` Brama na Starym Mieście w Warszawie '' i kilka wersji `` Święta trąbek '' .
Opis obrazu .
Artysta przedstawił portret ubogiej żydowskiej handlarki , sprzedającej cytryny na ulicy .
Jest to stara , odziana w łachmany kobieta , dźwigająca dwa kosze z owocami .
Stoi zwrócona twarzą do widza , patrząc wprost w oczy przechodzących obok niej ludzi .
Jest zmęczona życiem , a poorana zmarszczkami twarz wyraża smutek , zmęczenie i nadzieję na znalezienie nabywcy .
Kolorystyka obrazu jest zimna , sugeruje późną porę roku , być może jest chłodny , jesienny poranek .
Niewyraźne i przymglone tło potęguje wrażenie samotności , wyobcowania i zastygłego cierpienia , a realistycznie przedstawiona postać zamarła w beznadziejnym oczekiwaniu .
Odniesienia .
`` Żydówka z cytrynami '' pojawiła się na serii znaczków pocztowych pt .
`` Malarstwo polskie '' , wydanych w 1968 roku .
Autorem projektu znaczka był Andrzej Heidrich .
Stylizowany wizerunek fragmentów obrazu znalazł się na monecie o nominale 20 zł , z serii `` Polscy Malarze XIX/XX w. '' poświęconej Aleksandrowi Gierymskiemu , wyemitowanej przez NBP w 2006 roku .
Monetę wybito w Mennicy Polskiej SA w nakładzie 66 000 sztuk , a jej projektantem jest Roussanka Nowakowska .
