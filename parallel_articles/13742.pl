Żygląd
Żygląd ( niem .
`` Zeigland '' ) – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie chełmińskim , w gminie Papowo Biskupie .
Żygląd zajmuje powierzchnię 578,25 hektarów .
W 2013 roku w Żyglądzie działało 14 podmiotów gospodarczych .
Historia .
Średniowiecze .
Już w okresie wczesnego średniowiecza powstał tutaj gród , zbudowany na bazie stożka liczącego około 50 metrów średnicy , z majdanem około 12 metrów .
Ślady grodu znajdują się do dzisiaj .
