Żymlok
Żymlok ( niem .
Semmelwurst , śl-niem .
Sammelwurscht ) – tradycyjna śląska wędlina podrobowa , produkowana dawniej przy okazji świniobicia , by wykorzystać wszystkie składniki tuszy wieprzowej .
Poza obszarem Śląska występuje pod nazwą bułczanka .
Składniki .
Żymlok wyglądem przypomina kiełbaskę , a konsystencją kaszankę ( od której różni się dodatkiem miąższu bułki zamiast kaszy ) .
Nieodzownym składnikiem żymloka jest bułka ( po śląsku `` '' z niem .
słowa `` Semmel '' ) – stąd pochodzi nazwa tej potrawy .
