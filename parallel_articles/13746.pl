Żędowo
Żędowo – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie nakielskim , w gminie Szubin .
Podział administracyjny .
Wieś Żędowo ( Sodowo , Sandowo ) , należąca do klucza żnińskiego arcybiskupstwa gnieźnieńskiego , położona była w XVII wieku w powiecie kcyńskim województwa kaliskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 169 mieszkańców .
Jest 27. co do wielkości miejscowością gminy Szubin .
