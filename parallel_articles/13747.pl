Żyrardów ( stacja kolejowa )
Żyrardów – stacja kolejowa w Żyrardowie , w województwie mazowieckim .
Według klasyfikacji PKP ma kategorię dworca regionalnego .
Dworzec kolejowy wybudowano według projektu architekta Romualda Millera w 1922 roku .
W latach 2005–2007 miał miejsce gruntowny remont dworca .
Koszt remontu wyniósł ponad 3 mln zł i został sfinansowany ze środków własnych PKP SA .
Uroczyste otwarcie zmodernizowanego dworca odbyło się 9 sierpnia 2007 roku .
Na stacji jest kasa i automat biletowy Kolei Mazowieckich
Pasażerowie .
