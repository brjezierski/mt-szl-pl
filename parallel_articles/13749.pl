Żyrowa
Żyrowa ( niem .
`` Zyrowa '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie krapkowickim , w gminie Zdzieszowice .
Od 1950 Żyrowa należy do województwa opolskiego .
Integralne części wsi .
W przysiółku zachowały się pozostałości dawnego folwarku `` Waldhof '' .
Historia .
Historia miejscowości i pałacu sięga czasów średniowiecznych i jest związana jest z klasztorem minorytów ( z Jemielnicy ) , którzy na przełomie XIII i XIV wieku zbudowali pierwszy obiekt w miejscu obecnego pałacu .
13 marca 1631 roku Melchior Ferdynand de Gaschin kupił za 24 000 talarów dobra żyrowskie – m.in .
Żyrową .
„ Zyrowa ” była nazwą wioski do 1936 .
Podczas akcji germanizacyjnej nazw miejscowych i fizjograficznych na Górnym Śląsku historyczna nazwa niemiecka `` Zyrowa '' została w lipcu 1936 r. zastąpiona przez administrację nazistowską nazwą `` Buchenhöh '' .
Po kapitulacji Niemiec w 1945 r. ustalono nazwę Żyrowa .
W 2002 r. Żyrowa zdobyła nagrodę „ Najpiękniejszej Wsi Opolskiej ” .
