Kropidlak pomarańczowy
Kropidlak pomarańczowy ( `` Aspergillus ochraceus '' G .
Wilh . )
– gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Gatunek kosmopolityczny , występujący na całym świecie .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy zdiagnozowany został w 1877 r. przez Geroulda Wilhelma i według Index Fungorum nazwa podana przez tego autora jest prawidłowa .
Później przez różnych autorów opisywany był pod różnymi nazwami i zaliczany do różnych rodzajów , wyróżniono też w jego obrębie różne podgatunki , odmiany i formy .
Według Index Fungorum obecnie wszystkie one są synonimami `` Aspergillus ochraceus '' .
Morfologia .
Na podłożu Czapeka ( CzA ) w sposób ograniczony , na podłożu MEA szybciej , wzrastają płaskie kolonie ( czasem pobrużdżone ) koloru żółtopomarańczowego , ugrowego lub płowego ; spód kolonii jest bezbarwny .
Główki konidioforów mają promienisty układ zarodników .
Metule mają 15–20 × 5–6 μm długości , fialidy 7–11 × 2,0–3,5 μm .
Zarodniki są przejrzyste , okrągłe do okrągławych , nieco szorstkie , gładkościenne o średnicy 2,5 × 3.5 μm .
Występowanie .
Występuje na całym świecie w glebach .
Znajdowany na/w żywności , ziarnie zbóż , ziarnach kawy i przyprawach .
