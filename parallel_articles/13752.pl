Właściwości .
Rozpuszcza metale , tworząc amalgamaty ( z wyjątkiem żelaza , platyny , wolframu i molibdenu ) .
Wykazuje dużą lotność – w stanie nasycenia w temperaturze w powietrzu znajduje się 14 mg Hg na m³ .
Dawka progowa rtęci , czyli najwyższe stężenie uważane za bezpieczne , wynosi 0,05 mg Hg na m³ powietrza , dlatego rozlana rtęć stanowi potencjalne niebezpieczeństwo zatrucia .
