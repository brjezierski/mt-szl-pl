Rtęć
Rtęć ( Hg , , z ‘ wodne srebro ’ ) – pierwiastek chemiczny z grupy metali przejściowych .
Uznana za pierwiastek przez Lavoisiera .
Rtęć jest jedynym metalem występującym w warunkach normalnych w stanie ciekłym .
Rtęć występuje w skorupie ziemskiej w ilości 0,05 ppm .
