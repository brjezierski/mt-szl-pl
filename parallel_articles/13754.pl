Międzybrodzie Żywieckie
Międzybrodzie Żywieckie – wieś w gminie Czernichów , w powiecie żywieckim , w województwie śląskim .
Powierzchnia sołectwa wynosi 1003 ha , a liczba ludności 1463 , co daje gęstość zaludnienia równą 145,9 os./km² .
Międzybrodzie jest miejscowością o charakterze turystyczno-letniskowym .
Według danych z 1998 r. 1/4 domów to domy letniskowe , których w ostatnich latach ciągle przybywa .
Położenie .
Miejscowość położona jest na południowo-wschodnim brzegu Jeziora Międzybrodzkiego , utworzonego przez spiętrzenie wód na rzece Sole , w dolinie potoku Isepnica , na stokach gór Beskidu Małego – Żaru , Kiczery , Cisowej Grapy , Maleckiego i Jaworzyny .
Historia .
Historycznie miejscowość jest częścią księstwa oświęcimskiego .
