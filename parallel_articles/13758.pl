Železná Ruda
Železná Ruda ( niem .
`` Markt Eisenstein '' ) – miasto w Czechach , w powiecie Klatovy , w kraju pilzneńskim , przy Parku Narodowym Šumava i granicy z Niemcami ( Bayerisch Eisenstein ) .
Według danych z 31 grudnia 2016 powierzchnia miasta wynosiła 7 982 ha , a liczba jego mieszkańców 1 636 osób .
Ośrodek turystyczny i punkt wyjścia w góry Szumawy .
Historia .
Miejscowość wzmiankowana po raz pierwszy w XVI w. , prawa miejskie otrzymała w roku 1849 .
W drugiej połowie XIX w. wybudowano kolej z Pilzna do Bawarii .
Rozwój miasta jako ośrodka turystycznego został wstrzymany przez drugą wojnę światową i rozwinął się częściowo po otwarciu granic w początku lat 60 .
