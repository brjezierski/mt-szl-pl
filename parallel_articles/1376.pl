Aspergillus parasiticus
Aspergillus parasiticus Speare – gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Mikroskopijny grzyb chorobotwórczy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Kolonie na agarze Czapek z ekstraktem drożdżowym ( CYA ) w 7 dniu osiągają średnicę 5–7 cm .
Są aksamitne , ciemno żółtozielone z obfitym zarodnikowaniem na całej powierzchni .
Czasami tworzą białawe , potem czarne , kuliste , o średnicy 400–800 mikronów sklerocja .
Czasami sklerocja obejmują prawie całą kolonię , wówczas zarodnikowanie konidialne jest słabe .
Rewers plechy jest bezbarwny lub brązowawy .
