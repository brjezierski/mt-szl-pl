Pozelwa
Pozelwa ( lit .
`` Želva '' ) – miasteczko na Litwie , w okręgu wileńskim , w rejonie wiłkomierskim , położone ok. 22 km na wschód od Wiłkomierza , przy drodze Wiłkomierz-Malaty , nad rzeką Zelwą , dopływem Cesarki .
Siedziba gminy Pozelwa .
