Żylina
Żylina ( słow .
`` Žilina '' , węg .
`` Zsolna '' , niem .
`` Sillein '' ) − miasto w północnej Słowacji , siedziba władz kraju żylińskiego i powiatu Żylina .
Pod koniec 2019 roku , z liczbą mieszkańców wynoszącą ponad 80 tys. , Żylina zajmowała czwarte miejsce pod względem liczby ludności na Słowacji .
