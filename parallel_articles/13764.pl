Żmudź ( Natura 2000 )
Żmudź ( PLH060075 ) – projektowany specjalny obszar ochrony siedlisk , zatwierdzony jako obszar mający znaczenie dla Wspólnoty w marcu 2011 , a zlokalizowany w pobliżu wsi Żmudź-Kolonia w powiecie chełmskim ( województwo lubelskie ) .
Ma powierzchnię 44,1 hektara .
Opis ogólny .
Obszar obejmuje teren rezerwatu przyrody Żmudź wraz z przylegającym do niego rozległym kompleksem częściowo zalesionych muraw kserotermicznych ( `` Festuco-Brometea '' ) .
Na terenie obszaru występuje populacja obuwika pospolitego rosnącego przede wszystkim w luźnych zadrzewieniach sosnowych oraz w zaroślach jałowca pospolitego w i na murawach kserotermicznych .
Znajduje się tutaj także największa krajowa populacja lnu złocistego , a także
pewna liczba innych rzadkich i chronionych gatunków roślin , w tym łącznie pięć gatunków storczykowatych .
Rosną tu też formacje z jałowcem pospolitym na wrzosowiskach lub na wapiennych murawach .
Płaty muraw kserotermicznych w obszarze reprezentują zespół omanu wąskolistnego w odmianie typowej dla
podłoża węglanowego typowego dla Wyżyny Lubelskiej .
W stosunku do klasycznych muraw z omanem wąskolistnym opisanych dla Niecki Nidziańskiej różni się brakiem niektórych gatunków charakterystycznych .
Liczba gatunków charakterystycznych dla muraw kserotermicznych w składzie fitocenozy wynosi od 35,5 do 50,0 % .
Obuwik pospolity .
W obszarze rośnie populacja obuwika pospolitego , którego liczebność oceniono w 2015 na 238 pędy .
Populacja ta stanowi około 0,4 % populacji krajowej .
Stwierdzono 69 % pędów generatywnych , a efektywność zapylania wyniosła 37,8 % .
Występują tu także osobniki juwenilne .
Stanowisko położone jest w obrębie zasięgu gatunku w Polsce w sąsiedztwie innych populacji tego gatunku .
Populacja jest relatywnie mała i skoncentrowana na niewielkim obszarze przez co jest mniej odporna na negatywne oddziaływania .
Dziewięćsił popłocholistny .
W obszarze rośnie populacja dziewięćsiła popłocholistnego , którego liczebność oceniono w 2015 na 135 osobników .
Populacja stanowi prawdopodobnie więcej niż 2 % , a mniej niż 15 % zasobów gatunku w Polsce .
Stwierdzono cztery osobniki generatywne , występują liczne siewki .
Populacja ta jest nieizolowana , ale występuje na peryferiach zasięgu gatunku .
