Zdziar nad Sazawą
Zdziar nad Sazawą ( , ) – miasto w Czechach , w kraju Wysoczyna , siedziba powiatu Zdziar nad Sazawą .
Według danych z 31 grudnia 2003 powierzchnia miasta wynosiła 3 706 ha , a liczba jego mieszkańców 24 030 osób .
Historia .
Już około 1100 r. w pobliżu dzisiejszej lokalizacji zamku znajdowała się obronna osada kontrolująca przeprawę przez bród na rzece Sazawie .
Po założeniu w jej pobliżu klasztoru cystersów w roku 1252 , osada zaczęła zajmować obszar tzw .
