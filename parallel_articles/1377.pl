Aspergillus repens
Aspergillus repens ( Corda ) Sacc .
– gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Występuje powszechnie na różnego rodzaju produktach spożywczych i jest jednym z głównych grzybów strzępkowych biorących udział w psuciu się żywności .
Charakterystyka .
`` Aspergillus repens '' należy do organizmów umiarkowanie kserofilnych , czyli odpornych na wysokie temperatury .
Jest zdolny do wzrostu w warunkach obniżonej ilości wody na podłożach , które zostały wysuszone lub zagęszczone , przy tym może rozwijać się w obecności dużej ilości rozpuszczalnych substancji stałych , takich jak sole lub cukry .
Jego askospory w pewnych warunkach mogą przetrwać ogrzewanie w temperaturze 70–75 °C przez kilka minut .
`` A. repens '' powoduje psucie się przechowywanych towarów ( ziarna zbóż , nasiona oleiste , orzechy , przyprawy ) , wypieków mącznych ( chleb , ciasta owocowe ) , karmy dla zwierząt domowych , suszonych i fermentowanych produktów mięsnych i rybnych , serów oraz produktów o wysokiej zawartości cukru , takich jak jako wyroby cukiernicze , suszone owoce , dżemy i przetwory .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy opisał August Corda w 1842 r. jako odmianę `` Aspergillus glaucus '' ( `` Aspergillus glaucus '' var .
`` repens '' ) .
Pier Andrea Saccardo w 1882 r. podniósł go do rangi odrębnego gatunku .
Teleomorfa znana była jako `` Eurotium repens '' .
