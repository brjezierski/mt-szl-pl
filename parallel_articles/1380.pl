Aspergillus sydowii
Aspergillus sydowii ( Bainier & amp ; Sartory ) Thom & amp ; Church – gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Mikroskopijny grzyb chorobotwórczy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Trichocomaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy opisali w 1913 r. Georges Bainier i Auguste Theodore Sartory ) nadając mu nazwę `` Sterigmatocystis sydowii '' .
Do rodzaju `` Aspergillus '' przenieśli go Charles Thom i Margaret Church w 1934 r ..
Synonimy :
Siedlisko , morfologia i fizjologia .
Aspergillus sydowii to saprotrof występujący w glebie .
Może zanieczyszczać żywność .
Jest dominującym grzybem na pszenicy Qu , najszerzej stosowanym źródle surowych mikroorganizmów i surowych enzymów do warzenia chińskiego wina ryżowego .
