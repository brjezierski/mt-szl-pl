Aspergillus terreus
Aspergillus terreus ( `` Aspergillus terreus '' Thorn ) – gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Gatunek kosmopolityczny , występujący na całym świecie , głównie w rejonach tropikalnych .
Wykazuje oporność na amfoterycynę B .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy zdiagnozowany został w 1918 r. przez Grega Thorna i według Index Fungorum nazwa podana przez tego autora jest prawidłowa .
Później przez różnych autorów opisywany był pod różnymi nazwami i zaliczany do różnych rodzajów , wyróżniono też w jego obrębie różne podgatunki , odmiany i formy .
Według Index Fungorum obecnie wszystkie one są synonimami `` Aspergillus terreus '' :
Morfologia .
Na podłożu Czapeka ( CzA ) stosunkowo szybko , wzrastają kolonie o zwartej aksamitnej czasem kosmatej strukturze płaskie lub o płytkim radialnym pobrużdżeniu koloru od cynamonopłowego do brązowego , rzadko od pomarańczowego do oranżu cynkowego ;
spód kolonii jest żółty do brązowego .
Główki konidioforów z łańcuszkami zarodników w zwartych długich kolumnach .
Metule mają 5–7 × 2,0-2,5 μm długości , fialidy 5,5–7,5 × 1,0–2 μm .
Zarodniki są przejrzyste , okrągłe do elipsoidalnych , gładkie o średnicy 1,5 × 2,5 μm .
Występowanie .
Występuje w glebach na całym świecie , szczególnie w klimacie równikowym i podzwrotnikowym , najczęściej w Indiach .
Został stwierdzony na gnijących roślinach , kompoście oraz kiszonce z traw i siana .
