Asteroma coryli
Asteroma coryli ( Fuckel ) B. Sutton – gatunek grzybów z klasy `` Sordariomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Asteroma , Gnomoniaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungigi '' .
Po raz pierwszy takson ten zdiagnozował w 1880 roku Karl Wilhelm Fuckel nadając mu nazwę `` Leptothyrium coryli '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1980 roku Brian Charles Sutton , przenosząc go do rodzaju `` Asteroma '' .
Synonimy :
Charakterystyka .
Grzyb mikroskopijny , patogen roślinny powodujący powstawanie plam na porażonych roślinach .
Znana jest tylko jego anamorfa .
Za teleomorfę uważa się nieokreślony gatunek z rodzaju `` Gnomoniella '' .
W Polsce odnotowano występowanie na leszczynie pospolitej ( `` Corylus avellana '' ) i leszczynie tureckiej ( `` Corylus colurna '' ) .
