Asti ( Bakallbashi )
Asti , imię świeckie Kristaq Zoi Bakallbashi ( ur .
31 lipca 1974 w Durrësie ) – albański duchowny prawosławny , od 2012 biskup pomocniczy metropolii Tirany , z tytułem biskupa Ballshu .
Życiorys .
Święcenia prezbiteratu przyjął w 2009 r. 22 stycznia 2012 otrzymał chirotonię biskupią .
W czerwcu 2016 r. brał udział w Soborze Wszechprawosławnym na Krecie .
