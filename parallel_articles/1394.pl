( 1154 ) Astronomia – planetoida należąca do zewnętrznej części pasa głównego asteroid okrążająca Słońce w ciągu 6 lat i 88 dni w średniej odległości 3,39 au .
Została odkryta 8 lutego 1927 roku w Landessternwarte Heidelberg-Königstuhl w Heidelbergu przez Karla Reinmutha .
Nazwa planetoidy pochodzi od astronomii , nauki o ciałach niebieskich , ich budowie , ruchach , pochodzeniu i ewolucji .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 1154 ) 1927 CB .
