Asunción
Asunción ( wym .
[ ] ) – stolica Paragwaju , położona nad rzeką Paragwaj na zachodnim skraju Wyżyny Brazylijskiej , przy granicy z Argentyną .
Demografia .
Asunción jest największym miastem Paragwaju : zamieszkuje je 512 tys .
mieszkańców ( 2002 ) , czyli ok. 8 % ludności całego kraju , a cały zespół miejski `` Gran Asunción '' , w skład którego wchodzą takie miasta jak San Lorenzo , Fernando de la Mora , Capiatá , Lambaré , Luque , Mariano Roque Alonso , Limpio , Ñemby czy Villa Elisa , zamieszkuje prawie 1,7 mln osób .
Historia .
Miasto jest jedną z najstarszych hiszpańskich osad w Ameryce Południowej , a na pewno najstarszą położoną na wschód od Andów .
Nazywane jest czasem „ matką miast ” , ponieważ stąd wyruszały ekspedycje kolonialne , które zakładały nowe miasta na podbijanych terenach .
