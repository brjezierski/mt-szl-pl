Asyryjczycy
Asyryjczycy , Asyro-chaldejczycy ( arm .
ܥܡܐ ܐܬܘܪܝܐ/ܣܘܪܝܝܐ ) – naród semicki zamieszkujący północny Irak , Iran , południową Turcję i Syrię .
Asyryjczycy są uważani za jeden z rdzennych narodów na Bliskim Wschodzie .
Za ich ojczyznę uważa się tereny położone wokół Tygrysu i Eufratu .
Zamieszkanie .
Nawiązują do tradycji starożytnej Asyrii oraz Chaldei i uważają się za spadkobierców tych państw .
Posługują się językiem aramejskim i jego dialektami , używanymi na terenie Międzyrzecza przed podbojem arabskim .
Są też jednym z najstarszych narodów , które przyjęły chrześcijaństwo , nastąpiło to w II wieku n.e .
Obecnie zamieszkują przede wszystkim równinę Niniwy , położoną wzdłuż rzek Tygrys , Chabur i Wielki Zab , i stanowią tam ponad 80 % ludności .
Istnieje nieskonkretyzowany plan , aby utworzyć tam jednostkę administracyjną w postaci guberni lub autonomii .
Asyryjczycy zamieszkują także w rozproszeniu południowe obszary Turcji , część Syrii , Libanu , Iranu , pozostałej części Iraku i Jordanii .
W Iranie są oficjalnie uznaną mniejszością narodową .
Diaspora asyryjska istnieje na całym świecie , największa w Szwecji , Francji , Niemczech , USA , Kanadzie , Australii , Brazylii i Rosji .
W Polsce istnieje mała diaspora .
