Oszmiana ( stacja kolejowa )
Oszmiana ( biał .
Ашмяны , ros .
Ошмяны ) – stacja kolejowa w miejscowości Stacja Oszmiana , w rejonie oszmiańskim , w obwodzie grodzieńskim , na Białorusi .
Leży na linii Mińsk - Wilno .
Nazwa stacji pochodzi od miasta rejonowego Oszmiana ( oddalanego ok. 25 km w linii prostej od stacji ) .
Stacja istniała przed II wojną światową .
W dwudziestoleciu międzywojennym leżała w Polsce .
Ze stacji odchodzi linia do Bobrownik , będąca kolejowym połączeniem Białoruskiej Elektrowni Jądrowej .
