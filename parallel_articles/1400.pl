At-Tall ( Palestyna )
At-Tall ( ) – dawna palestyńska wieś położona 14 km na północny wschód od Akki w Dystrykcie Akki w Mandacie Palestyny .
Wyludniona w wyniku izraelskiego ataku wojskowego podczas wojny w latach 1947–1948 przez brygadę Karmeli Sił Obronnych Izraela .
Historia .
Bliźniacze wioski Al-Tall i pobliskie An-Nahr były miejscami starożytnych osad na szczycie Tel Kabri .
Ostatnie wykopaliska wskazują na zamieszkanie tych ziem już w XVIII wieku p.n.e .
Lista ludności z około 1887 roku wykazała , że wieś zamieszkiwało 275 mieszkańców , wszyscy byli muzułmanami .
W 1945 populacja Al-Tall wynosiła 300 osób .
Współcześnie .
Al-Tall zostało zajęte i wyludnione przez Brygadę Karmeli podczas wojny w latach 1947–1948 .
Obszar został włączony do państwa Izrael , ale teren wsi nie został przejęty przez żadną gminę .
