Morfologia .
Kolonie na białym agarze ziemniaczano-dekstrozowym zwykle z wieloma wąskimi pasmami grzybni w grzybni powietrznej .
Komórki pierwotnych strzępek na przedniej krawędzi kolonii , zwykle o szerokości 4,5–9 µm i długości do 350 µm , z jednym lub większą liczbą sprzążek przy przegrodzie .
Strzępki wtórne powstają bezpośrednio pod dystalną przegrodą komórkową i często rosną przyciśnięte do strzępek pierwotnych .
Strzępki trzeciorzędowe i kolejne są wąskie ( szerokość 1,5–2 µm ) , mają stosunkowo krótkie komórki i szeroki kąt rozgałęzienia nie związany ściśle z przegrodą i zwykle nie mają sprzążek .
Sklerocja tworzą się na powierzchni kolonii , przeważnie są kuliste , nieznacznie spłaszczone w dolnej części , przeważnie o średnicy 1–2 mm w stanie świeżym , lekko kurczące się po wyschnięciu , z gładką lub płytko wgłębioną błyszczącą powierzchnią .
Ich ściana jest równomiernej grubości i lekko wybarwiona .
Kora i rdzeń sklerocjów zawierają pęcherzyki z materiałem zapasowym , komórki skórne nie .
Hymenium na podłożu agarowym o barwie od białej do ochrowej , podkładki rzadkie lub skupione w nieregularnych masach .
