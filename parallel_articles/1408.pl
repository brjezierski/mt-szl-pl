Jesiotr ostronosy
Jesiotr ostronosy , jesiotr atlantycki ( `` Acipenser oxyrinchus '' , czasem pisane `` oxyrhynchus '' ) – gatunek wędrownej , anadromicznej ryby z rodziny jesiotrowatych ( Acipenseridae ) .
Jedyny jesiotr naturalnie występujący w Bałtyku .
Podgatunki i historia ewolucyjna .
Wyróżniono dwa podgatunki jesiotra ostronosego :
Dawniej uważano , że w Bałtyku żył jesiotr zachodni ( `` Acipenser sturio '' ) .
W 1996 w Estonii złowiono wielkiego jesiotra i zbadano jego DNA .
Okazało się , że jest bliżej spokrewniony z jesiotrem ostronosym ze wschodnich brzegów Ameryki Północnej niż z jesiotrem zachodnim .
Również analizy genetyczne pradawnych populacji jesiotrów z Polski – jednej z III wieku p.n.e. , a drugiej z IX–XIII wieku n.e .
– potwierdziły ich bliskie pokrewieństwo z kanadyjskimi populacjami `` A. oxyrinchus '' .
Jesiotr ostronosy skolonizował Bałtyk prawdopodobnie 4–5 tysięcy lat temu , a od co najmniej 2 tysięcy jest tam dominującym gatunkiem jesiotra .
Niektóre okazy archeologiczne i muzealne mają allele typowe dla jesiotra zachodniego , co wskazuje , że między tymi gatunkami doszło do hybrydyzacji , prawdopodobnie jeszcze zanim jesiotry ostronose skolonizowały Bałtyk .
Obecnie populacja bałtycka , określana nazwą jesiotr bałtycki , uznawana jest za wymarłą , choć próbuje się ją odtworzyć przy wykorzystaniu materiału biologicznego z St. John River .
Populacje zasiedlające kanadyjskie rzeki St. John i St. Lawrence powstały przypuszczalnie , odpowiednio , 9000 i 7400 lat temu .
Budowa .
Ciało lekko spłaszczone grzbieto-brzusznie .
Rostrum duże i silnie spłaszczone , u dorosłych osobników stanowi połowę długości głowy .
Osiąga do 4 m i 350 kg masy ciała , przeciętnie 2,5 m i od 100 do 150 kg .
Linia boczna jest wyraźnie widoczna .
Odżywianie .
Jego pokarm stanowi fauna denna , którą wygrzebuje z miękkiego podłoża ryjkowatym pyskiem .
Głównie są to mięczaki , skorupiaki , larwy owadów , niekiedy drobne ryby ( babki i dobijaki ) .
Rozród .
Jesiotr ostronosy do rozrodu potrzebuje chłodniejszej wody ( 13,3–17,8 °C , a jesiotr zachodni co najmniej 20 °C ) .
Masa ciała dojrzałych wędrujących samców wynosi 30–45 kg , a samic 50–110 kg .
Długość życia nawet 35 lat .
