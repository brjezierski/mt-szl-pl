Atotonilco El Alto
Atotonilco El Alto – miasto w północno-wschodniej części meksykańskiego stanu Jalisco , w regionie Ciénega , położone około 90 km na wschód od stolicy stanu - Guadalajary .
Jest siedzibą władz gminy Atotonilco El Alto .
Miasto w 2010 r. zamieszkiwało 26 847 osób , natomiast ludność całej gminy liczyła 57 717 osób .
Klimat Atotonilco El Alto jest subtropikalny , według klasyfikacji Wladimira Köppena , należy do umiarkowanie ciepłych ( `` Cwa '' ) , z łagodnym , umiarkowanie wilgotnym latem i suchą zimą .
