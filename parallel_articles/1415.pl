Atyrau
Atyrau ( kaz .
Атырау ; do 1991 Gurjew , ros .
Гурьев ) – miasto w Kazachstanie , ośrodek administracyjny obwodu atyrauskiego .
Port nad rzeką Ural , w pobliżu jej ujścia do Morza Kaspijskiego .
Miasto położone na granicy dwóch kontynentów : Europy oraz Azji .
Wraz z Aktau , największy kazachstański ośrodek miejski nad Morzem Kaspijskim .
Miasto zostało założone w 1640 .
Obecnie jest to duży ośrodek przeróbki ropy naftowej ( miasto połączone jest rurociągiem naftowym z położonym w odległości 720 km rosyjskim miastem Orsk ) i przemysłu rybnego .
Poza tym rozwinął się tutaj przemysł maszynowy , spożywczy i materiałów budowlanych .
W porcie rzecznym prowadzony jest remont statków .
W Atyrau znajduje się muzeum regionalne .
Klimat .
Miasto leży w strefie klimatu subtropikalnego pustynnego , z wyraźnym wpływem klimatu kontynentalnego .
Występują tu zimne zimy ( przymrozki występują w dzień i nocy ) oraz upalne lata ( od czerwca do sierpnia , choć lato tutaj trwa od maja do września ) .
W miesiącach zimowych - grudzień , styczeń , luty - przez 1/3 miesiąca może padać śnieg .
