Aubigny ( Wandea )
Aubigny – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Wandea .
W 2013 roku populacja ludności wynosiła 3557 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Aubigny oraz Les Clouzeaux – utworzono nową gminę Aubigny-les-Clouzeaux .
Siedzibą gminy została miejscowość Aubigny .
