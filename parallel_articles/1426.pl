Auderghem
Auderghem ( nid .
Oudergem ) – jedna z 19 gmin w Regionie Stołecznym Brukseli , w Belgii .
1 stycznia 2015 liczyła 31 985 mieszkańców .
Historia .
W 1794 roku żołnierze rewolucji francuskiej postanowili rozdzielić trzy leśne wioski Auderghem , Watermael i Boitsfort-Bosvoorde , które były od wieków jedną jednostką , na trzy różne gminy .
W 1811 roku Napoleon postanowił zjednoczyć trzy wsie za pomocą cesarskiego dekretu w jedną jednostkę administracyjną .
Ale Auderghem został wycofany z tego związku w akcie królewskim , pozostawiając Watermael-Boitsfort oddzielną gminą .
W 1863 roku Auderghem stał się samodzielną gminą z 1600 mieszkańców .
