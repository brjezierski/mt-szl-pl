Auerbach in der Oberpfalz
Auerbach in der Oberpfalz , Auerbach i.d.OPf .
– miasto w Niemczech , w kraju związkowym Bawaria , w rejencji Górny Palatynat , w regionie Oberpfalz-Nord , w powiecie Amberg-Sulzbach .
Leży w Jurze Frankońskiej , około 32 km na północny zachód od Amberga , przy drodze B85 i B470 .
Historia .
W 1314 Auerbach otrzymało prawa miejskie .
