August Schneider
August Schneider ( ur .
20 października 1851 w Lubinie , zm .
25 czerwca 1929 w Dreźnie ) – niemiecki adwokat i notariusz , burmistrz Katowic .
Biografia .
Był synem urzędnika Sądu Rejonowego Heinricha E. Schneidera i wnukiem radnego Otto Heinricha Schneidera .
Studiował we Wrocławiu prawo .
W 1882 roku został wpisany na listę prawników przez Sąd Rejonowy w Mysłowicach , gdzie prowadził również własną kancelarię .
W 1890 lub 1891 został wybrany burmistrzem Katowic .
W 1902 roku został wybrany po raz kolejny , ale złożył dymisję .
Za jego rządów w 1896 powstała pierwsza linia tramwajowa , w 1895 r. szpital , a w 1898 r. doprowadzono prąd z elektrowni w Chorzowie i rozpoczęto budowę kanalizacji .
Zasługi Schneidera uczczono nadaniem jego imienia jednej z głównych ulic miasta `` August-Schneider-Straße '' ( obecnie ul .
Adama Mickiewicza ) .
Odznaczony Rote Adlerorden IV klasy i Königlicher Preußischer Kronenorden III klasy .
