Austria
Austria ( niem .
`` Österreich '' , ) , Republika Austrii ( niem .
`` Republik Österreich '' , ) – państwo śródlądowe położone w Europie Środkowej , federacja dziewięciu krajów związkowych ze stolicą w Wiedniu .
Od 1995 Austria należy do Unii Europejskiej .
Geografia .
Powierzchnia Austrii wynosi 83 854 km² .
Ponad połowa kraju znajduje się w Alpach .
Najwyższym szczytem jest Großglockner , sięgający 3797 m n.p.m .
Na wschodzie Austrii znajduje się Kotlina Wiedeńska , a także Mała Nizina Węgierska z Jeziorem Nezyderskim .
Przez Austrię przepływa Dunaj ( na odcinku o długości 350 km ) .
Regiony górskie gęsto porasta las .
24 % powierzchni Austrii znajduje się pod ochroną .
Zagrożonymi gatunkami są : kozice , świstaki i koziorożce .
Długość całkowita granic lądowych : 2832 km
Klimat .
Austria znajduje się pod wpływem klimatu umiarkowanego , jednak ze względu na położenie i rzeźbę występują lokalne różnice klimatyczne .
Dokładnie Austria znajduje się pod wpływem trzech typów mas powietrza .
