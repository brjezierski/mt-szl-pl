Na obszarze całych Austro-Węgier funkcjonowało 20 ciał przedstawicielskich , w których zasiadali przedstawiciele 11 narodowości i 27 partii .
Najpowszechniej używany był język niemiecki , jednakże nigdy nie został językiem państwowym .
Nie rozumiała go znaczna część policjantów , wielu posłów i niejeden namiestnik cesarski .
Dwujęzyczni byli z reguły urzędnicy , choć w Czechach panowała nieprzezwyciężalna awersja Niemców do języka czeskiego .
Urzędnicy , którym fachowości nie odmawiano , byli odporni na naciski polityczne , zachowywali niezależność , mieli wysokie poczucie ważności i godności swego zawodu oraz służyli prawu , które dobrze znali .
Flaga .
Ze względu na specyficzną formę państwowości Austro-Węgry nie posiadały jednej oficjalnej flagi państwowej .
W praktyce , w zależności od sytuacji do reprezentowania wspólnego państwa stosowano różne flagi .
Najczęściej wykorzystywano w tym celu flagę czarno-żółtą ( rodu Habsburgów i Cesarstwa Austrii ) oraz czerwono-biało-zieloną ( Królestwa Węgier ) .
