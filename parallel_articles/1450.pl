Authon ( Alpy Górnej Prowansji )
Authon – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 38 osób , a gęstość zaludnienia wynosiła 1 osób/km² .
W styczniu 2015 r. Authon zamieszkiwało 51 osób , przy gęstości zaludnienia wynoszącej 1,3 osób/km² .
