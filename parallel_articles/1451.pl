Samochód sportowy
Samochód sportowy – klasa samochodów , której głównym celem jest możliwie najlepsze prowadzenie .
Przeważnie są to małe samochody , z niskim środkiem ciężkości , utwardzonym zawieszeniem , precyzyjnym układem kierowniczym i dobrym stosunkiem mocy do masy .
Przeważnie są to samochody dwuosobowe , lub 2+2 .
W klasycznym rozumieniu są to głównie auta typu Kabriolet i Coupe .
Samochody sportowe są mniejsze , mniej luksusowe i lżejsze od Grand Touring .
