Autonomiczna Republika Krymu
Autonomiczna Republika Krymu ( , trb .
`` Awtonomna Respublika Krym '' ; , trb .
`` Awtonomnaja Riespublika Krym '' ; , trb .
`` Kyrym Muchtar Dżumchurieti '' ) – autonomiczna republika znajdująca się na terenie Ukrainy .
Obejmuje Półwysep Krymski , leżący na północnym wybrzeżu Morza Czarnego .
Stolicą republiki jest Symferopol .
W grudniu 2013 r. miała 1,97 mln mieszkańców .
Terytorium Ukrainy w części obejmującej Autonomiczną Republikę Krymu zostało w 2014 anektowane przez Federację Rosyjską , która utworzyła na zajętym obszarze jednostkę administracyjną o nazwie Republika Krymu .
Geografia .
Autonomiczna Republika Krymu ma powierzchnię 26 081 km² .
Teren ten obejmuje niemal cały obszar Półwyspu Krymskiego ( oprócz obszaru Sewastopola i północnych fragmentów Mierzei Arabackiej ) .
W części wschodniej , obok cieśniny Kerez , wznoszą się góry Jaiłu , otaczające cały brzeg południowy i ku północy rozdzielające na kilka równoległych łańcuchów gór , pokrytych gęstym lasem .
Na północy przechodzą w step , zajmujący większą część półwyspu .
Najwyższym szczytem Gór Krymskich jest Roman-Kosz o wysokości 1545 m n.p.m. , drugim szczytem – Czatyrdah ( starożytne Mons Trapezus , 1527 m n.p.m .
) .
Na południu Krymu znajduje się dużo malowniczych miejscowości , warowni , ruin starożytnych gmachów , klasztorów , meczetów tatarskich , pięknych ogrodów i gajów oliwnych , natomiast w części północnej Krymu się ich prawie nie spotyka .
Klimat łagodny umożliwia rozwój roślinności o charakterze podzwrotnikowym .
