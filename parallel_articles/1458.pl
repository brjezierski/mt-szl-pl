Auverse
Auverse – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku jej populacja wynosiła 462 mieszkańców .
W dniu 15 grudnia 2016 roku z połączenia 14 ówczesnych gmin – Auverse , Breil , Broc , Chalonnes-sous-le-Lude , Chavaignes , Chigné , Dénezé-sous-le-Lude , Genneteil , Lasse , Linières-Bouton , Meigné-le-Vicomte , Méon , Noyant oraz Parçay-les-Pins – powstała nowa gmina Noyant-Villages .
Siedzibą gminy została miejscowość Noyant .
