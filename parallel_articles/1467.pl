Awinion ( fr .
`` Avignon '' , prowans .
`` Avinhon '' ) – miasto na południu Francji ( w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Vaucluse ) , u podnóża wapiennego wzgórza , na lewym brzegu Rodanu , przy ujściu do niego rzeki Durance .
Nad miastem góruje pałac papieski ( `` Palais des Papes '' ) .
Według danych na rok 1990 gminę zamieszkiwało 86 939 osób , a gęstość zaludnienia wynosiła 1342 osoby/km² ( wśród 963 gmin regionu Prowansja-Alpy-Lazurowe Wybrzeże Avignon plasuje się na 5. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 100 . ) .
