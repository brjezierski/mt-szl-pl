Avranches
Avranches – miejscowość i gmina we Francji , w regionie Normandia , w departamencie Manche .
W 2013 roku populacja ówczesnej gminy wynosiła 8528 mieszkańców .
Dnia 1 stycznia 2019 roku połączono dwie wcześniejsze gminy : Saint-Martin-des-Champs oraz Avranches .
Siedzibą gminy została miejscowość Avranches , a nowa gmina przyjęła jej nazwę .
To miasteczko jest popularne wśród turystów głównie z tego powodu , że znajduje się w bezpośredniej bliskości Mont Saint Michel i stanowi dobrą bazę wypadową dla osób pragnących odwiedzić to miejsce .
Z ogrodu kwiatowego w Avranches rozciąga się widok na Mont Saint Michel , popularny szczególnie po zachodzie słońca .
W Avranches zgromadzono także cenne rękopisy pochodzące ze scriptorium słynnego opactwa .
