Język awarski
Język awarski ( aw .
, ) – język kaukaski używany przez Awarów , głównie na obszarze Dagestanu , w pewnej liczbie także na pozostałym obszarze Federacji Rosyjskiej oraz na terenie Azerbejdżanu , Gruzji i Turcji .
Awarski należy do zespołu awaro-didojskiego , tworzącego podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Język ten używany jest , według różnych szacunków , przez ok. 556 tys .
osób ( 1989 r. ) do ponad 650 tys .
Głównymi użytkownikami tego języka są Awarowie , choć pełni on także rolę lingua franca oraz standardu literackiego dla wielu dagestańskich plemion , których języki nie wykształciły normy pisanej .
Wyróżnia się kilka głównych dialektów języka awarskiego oraz drobniejsze gwary lokalne .
Podstawowy ich podział polega na przeciwstawieniu sobie dialektów północnych i południowych .
