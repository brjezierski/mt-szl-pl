Awdijiwka
Awdijiwka ( ) – miasto na Ukrainie w obwodzie donieckim .
W mieście znajduje się duży kombinat koksochemiczny .
Do 2017 roku funkcjonowała jedna linia tramwajowa , która łączyła centrum Awdijiwki z zakładami koksochemicznymi .
Historia .
Awdijiwka jest jedną z najstarszych miejscowości obwodu donieckiego .
Pierwsza osada na terenie współczesnego miasta została założona w połowie XVIII wieku przez osadników z guberni kurskiej , woroneskiej i połtawskiej .
W 1778 r. powstała wieś , która przeszła na własność państwową .
Została nazwana na cześć pierwszego osadnika , Awdiejewa .
W połowie lat 80 .
XIX wieku przez obszar , na którym obecnie znajduje się miasto , przejeżdżała Kolej Katarzyny ; wybudowano tu stację kolejową .
Awdijiwka ucierpiała w wyniku ludobójstwa dokonanego przez władze ZSRR na ludności ukraińskiej w latach 1932–1933 , liczba ustalonych ofiar to 485 osób .
