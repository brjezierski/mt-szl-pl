Ayamonte
Ayamonte – miasto i port jachtowy w Hiszpanii , w Andaluzji , przy granicy z Portugalią , nad rzeką Gwadiana .
W 2012 liczyło 20 968 mieszkańców .
Miasto jest rozwijającym się ośrodkiem turystycznym .
Wokół starego miasta położonego na półwyspie nad ujściem Gwadiany do Zatoki Kadyksu ( Atlantyk ) , szereg nowych osiedli wakacyjnych złożonych z willi i apartementowców .
Dwa pola golfowe .
Na wyspie Isla Canela szeroka piaszczysta plaża ( część wybrzeża Costa de la Luz ) .
W odległości około 3 kilometrów od miasta , na granicy z Portugalią znajduje się nowoczesny most wiszący ( rok budowy 1991 ) .
