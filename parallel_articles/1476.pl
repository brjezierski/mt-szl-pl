Ayotlán
Ayotlán – miejscowość we wschodniej części meksykańskiego stanu Jalisco , w regionie Ciénega , położona około 100 km na południowy wschód od stolicy stanu – Guadalajary .
Jest siedzibą władz gminy Ayotlán .
Miasto w 2010 r. zamieszkiwały 11 724 osoby , natomiast ludność całej gminy liczyła 38 291 osób .
Klimat Ayotlán jest subtropikalny , charakterystyczny dla wysokich równin pomiędzy zwrotnikami Meksyku , według klasyfikacji Wladimira Köppena , należy do klimatów umiarkowanych ( `` Cwb '' ) , z wilgotnym latem i suchą zimą .
