Az-Zuwajrat ( arab .
الزويرات ; fr .
`` Zouérate '' ) – miasto w północnej Mauretanii w pobliżu góry Kidjat Idżdżil , stolica regionu administracyjnego Tiris Zammur , ośrodek eksploatacji złóż rudy żelaza ( wspólnie z kopalniami w pobliskim Fudajrik ) – niedaleko miasta mieści się kopalnia Tazadit .
Miasto połączone jest koleją mauretańską z portem w Nawazibu .
