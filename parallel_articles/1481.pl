Azja
Azja ( gr .
`` Asía '' , łac .
`` Asia '' ) – część świata , razem z Europą tworząca Eurazję , największy kontynent na Ziemi .
Z powodów historycznych i kulturowych sama Azja bywa również nazywana kontynentem ( zob .
alternatywne listy kontynentów ) .
Nazywana jest często kontynentem wielkich kontrastów geograficznych .
Sąsiaduje z Europą od zachodu , Afryką od południowego zachodu , Oceanem Indyjskim i Australią od południowego wschodu oraz Pacyfikiem od wschodu .
Dokładny przebieg zachodniej granicy geograficznej przedstawiony jest w haśle granica Europa-Azja .
Obszar Azji to 44,6 mln km² powierzchni lądów , co stanowi około 30 % powierzchni wszystkich lądów .
Ma charakter wyżynno – górski ( wyżyny stanowią 75 % powierzchni tego kontynentu – średnia wysokość Azji stanowi prawie 1000 m n.p.m . ) .
Azję zamieszkuje 4436,33 mln ludzi , co daje 99,9 osób na km² ( dane z 2016 roku ) .
Skrajnym punktem na północy jest przylądek Czeluskin ( 77°43′N ) , na południu przylądek Piai ( 1°16′N ) , na zachodzie zaś przylądek Baba ( 26°05′E ) , a na wschodzie przylądek Dieżniowa ( 169°40′W ) .
Rozciągłość równoleżnikowa równa jest około 8600 km , zaś rozciągłość południkowa 8400 km .
Linia brzegowa , wraz z wyspami , wynosi powyżej 70 000 km długości .
Nazwa .
Nazwa kontynentu trafiła do języka polskiego z języka greckiego za pośrednictwem łaciny .
Co do pierwotnego źródła nazwy brak jest pewności .
Wskazywane są zarówno powiązania z asyryjskim `` assu '' , znaczącym `` wstający ( o słońcu ) '' , akadyjskim `` aṣû '' , tj .
`` wejściem , miejscem jasnym , krajem wschodu '' , anatolijską nazwą federacji 22 państw `` Assuwa '' , greckim słowem `` asis '' oznaczającym `` namuł rzeczny '' .
Początkowo nazwa ta odnosiła się jedynie do Azji Mniejszej .
Około 450 r. p.n.e .
odróżniono Azję Właściwą od Azji Mniejszej .
Azja ( Asja ) była imieniem nimfy okeanidy w mitologii greckiej .
Tak również nazywano i rodzaj żyta .
Prawdopodobne jest również , że nazwa „ Azja ” pochodzi od greckiego określenia `` Asios leimon '' ( dosł .
`` łęg azyjski '' ) .
Asios utworzone byłoby od greckiego `` asis '' , oznaczającego muł rzeczny .
Wymieniony łęg azyjski leżał u ujścia rzeki Kaistru w pobliżu Efezu , a więc w Azji Mniejszej .
Z chwilą podboju Azji Mniejszej przez Persów Grecy nazwali Azją obszary leżące na wschód od Morza Egejskiego .
Dopiero później nazwą tą objęto cały kontynent .
