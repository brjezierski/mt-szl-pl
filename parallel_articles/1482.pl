Алагир ) – miasto w Rosji , w położonej na północnym Kaukazie Republice Północnej Osetii-Alanii , ośrodek administracyjny rejonu ałagirskiego .
Miasto liczy 19 678 mieszkańców ( 1 stycznia 2021 ) , położone jest nad rzeką Ardon ( lewy dopływ Tereku ) , 54 km na zachód od Władykaukazu .
Ałagir został założony w 1850 r. przy kopalni srebra i ołowiu .
Prawa miejskie uzyskał w roku 1938 .
Znajduje się tu dyrekcja Rezerwatu Północnoosetyjskiego .
