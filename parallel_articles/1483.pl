Ałczewsk
Ałczewsk ( ) – miasto na południowo-wschodniej Ukrainie , w obwodzie ługańskim , w Donieckim Zagłębiu Węglowym , oddalone 45 km od Ługańska .
Stacja kolejowa .
Od 1961 miasto trzy razy zmieniało nazwy .
Od 1931 do 1961 nazywało się Woroszyłowśk ( Ворошиловск ) , później do 1991 Kommunarśk ( Коммунарск ) , od 1991 nazywa się Ałczewsk .
Historia .
Miasto zostało założone w 1895 jako miejscowość mieszkalna dla pracowników tutejszej huty żelaza , przy węźle kolejowym Juriewka na kolei Jekatierińskiej .
W 1930 roku zaczęto wydawać gazetę .
Największa ekspansja mieszkańców miała miejsce w latach 50. i 60 .
XX wieku , kiedy miasto stało się jednym z największych centrów przemysłowych okręgu donieckiego .
Obecnie w hucie żelaza wytapia się jedną czwartą surówki w obwodzie .
Od 1991 w granicach Ukrainy .
Do 1997 r. istniały tutaj dwie wyższe uczelnie ( № 40 i № 81 ) oraz technikum przemysłowe – metalurgiczne .
W maju 1997 r. wyższe uczelnie № 81 zostało zlikwidowano .
Obecny Ałczewsk to duży ośrodek kulturalny i naukowy .
Główne gałęzie przemysłu : metalurgia ( huta żelaza ) , koksownia , materiałów budowlanych , elektromechaniczny , lekki i spożywczy .
W Ałczewsku ma siedzibę klub piłkarski Stal , grający w sezonie 2005/2006 w Wyszczej Lidze .
Od 2014 roku znajduje się pod kontrolą Ługańskiej Republiki Ludowej .
