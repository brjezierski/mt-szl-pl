Ałmaty
Ałmaty ( stara nazwa Ałma-Ata ; , ) – największe miasto i do 1997 stolica Kazachstanu , położone w południowo-wschodniej części kraju , na wysokości ok. 700 m n.p.m .
u podnóża Zailijskiego Ałatau ( Tienszan ) , w pobliżu granicy z Kirgistanem , na terenie charakteryzującym się dużą aktywnością sejsmiczną .
Miasto stanowi główny ośrodek przemysłowy , kulturalny i naukowy Kazachstanu : znajduje się w nim Akademia Nauk oraz liczne uczelnie .
Nazwa miasta .
Według zaleceń Komisji Standaryzacji Nazw Geograficznych poza Granicami Rzeczypospolitej Polskiej , przyjętych podczas 73. posiedzenia ( 5 września 2012 ) , nazwa `` Ałmaty '' to główna polska nazwa miasta , nazwą wariantową pozostaje zaś `` Ałma-Ata '' .
Nie ustalono jednoznacznie tego , czy egzonim winien podlegać deklinacji .
Komisja Standaryzacji Nazw Geograficznych nie zajmuje w tej sprawie określonego stanowiska .
Urzędnicy z Ministerstwa Spraw Zagranicznych i z Ambasady RP w Astanie nie odmieniają tej nazwy , natomiast `` Wielki słownik ortograficzny '' wydany przez Wydawnictwo Naukowe PWN podaje dopełniacz `` Ałmatów '' .
Historia .
Na trasie starego Jedwabnego Szlaku w 1854 r. Rosjanie założyli twierdzę `` Zailijskie '' ( `` Zailijskoje '' , to znaczy znajdujące się za rzeką Ili ) z 470 żołnierzami i oficerami załogi , wokół której zaczęła wyrastać osada .
W 1867 r. liczyła 10 tys .
mieszkańców , została przemianowana na `` Wierny '' ( `` Wiernyj '' , tzn .
„ wierny , oddany ” ) i uzyskała prawa miejskie .
W 1921 przeprowadzono derusyfikację nazwy na rzecz `` Ałma-Ata '' , ale forma ortograficzna różniła się od tej , którą stosowano w języku kazachskim .
W 1927 r. przeniesiono z Kyzyłordy do Ałma-Aty stolicę będącej w składzie Rosji Radzieckiej kazachskiej republiki autonomicznej .
W 1929 r. miasto miało 45 tys .
mieszkańców .
