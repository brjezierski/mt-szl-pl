Języki ałtajskie
Języki ałtajskie – grupa języków , wcześniej traktowanych jako rodzina językowa .
Obecnie uznaje się języki ałtajskie raczej za ligę językową , czyli zespół języków , których podobieństwa wynikają ze wzajemnych interakcji .
Badania przeprowadzone metodą triangulacji technik antropologicznych , językoznawczych i archeologicznych wskazują na wspólne źródło wszystkich języków – język transeurazjatycki .
Grupa języków ałtajskich dzieli się na trzy rodziny :
Językami ałtajskimi posługuje się ponad 152 mln osób , zamieszkujących rozległe obszary Azji , od Azji Mniejszej przez Azję Środkową i środkową Syberię , aż po chiński i rosyjski Daleki Wschód .
Ze względu na swoje podobieństwo do języków uralskich łączone są w jedną ligę uralo-ałtajską .
Do języków ałtajskich bywają zaliczane także języki japoński i koreański .
