B ( dźwięk )
B – obniżony bemolem dźwięk h ( odległy o kwartę czystą od f ) .
Jego częstotliwość dla b¹ wynosi około 466,2 Hz .
Stanowi tonikę gam B-dur i b-moll .
Nazwa tego dźwięku stanowi wyjątek na tle pozostałych nazw dźwięków obniżonych pojedynczym bemolem , w przypadku tworzenia których stosuje się dodaną do nazwy dźwięku obniżanego końcówkę `` -es '' .
Enharmonicznie dźwięki o tej samej wysokości to : ais oraz ceses .
Takie oznaczenie stosowane jest w Polsce , Niemczech i krajach skandynawskich .
W pozostałych krajach litera B oznacza „ polski ” dźwięk H ( pół tonu poniżej C , 493,9 Hz ) , a H obniżone o pół tonu nazywa się „ bes ” lub „ b-flat ” ( B z bemolem ) .
Niejednoznaczność nomenklatury wynika z używanego w średniowieczu nieprzejrzystego systemu notacji , opierającego się o pismo gotyckie , w którym litery B i H wyglądały podobnie .
Różne nazwy tych samych dźwięków powodują pewne problemy w odczytywaniu zagranicznych wydań nut .
Również polskie wydania nut muzyki rozrywkowej często używają notacji zapożyczonej z wydań amerykańskich .
Szwecja w 1990 roku wprowadziła w edukacji muzycznej zmiany mające na celu częściową redukcję problemu .
