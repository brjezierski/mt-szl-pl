Baar ( Schwaben )
Baar ( Schwaben ) – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Szwabia , w regionie Augsburg , w powiecie Aichach-Friedberg , wchodzi w skład wspólnoty administracyjnej Pöttmes .
Leży około 20 km na północny zachód od Aichach .
Polityka .
Wójtem gminy jest Leonhard Kandler z WG , rada gminy składa się z 12 osób .
