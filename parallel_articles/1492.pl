Babia Góra ( Szklary )
Babia Góra ( 461 m ) – wzgórze w miejscowości Szklary , w województwie małopolskim , w powiecie krakowskim , w gminie Jerzmanowice-Przeginia .
Znajduje się na Wyżynie Olkuskiej będącej częścią Wyżyny Krakowsko-Częstochowskiej .
Babia Góra wznosi się na prawych zboczach Doliny Szklarki , naprzeciwko skały Brodło .
Po południowo-wschodniej stronie Babiej Góry znajduje się drugie , niższe wzgórze 453 m. Obydwa porośnięte są lasem sosnowym .
