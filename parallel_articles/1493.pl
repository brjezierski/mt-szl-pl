Baborów ( stacja kolejowa )
Baborów – stacja kolejowa położona w Baborowie w województwie opolskim .
Posiada połączenia m.in .
z Raciborzem , Kędzierzynem-Koźle i Pilszczem .
Historia .
Stacja `` Bauerwitz '' powstała w dzisiejszym Baborowie w 1855 roku w związku z otwarciem linii kolejowej z Głubczyc do Raciborza ( dziś linia kolejowa nr 177 ) .
W 1908 otwarto odcinek Baborów – Polska Cerekiew linii kolejowej 195 , który połączył stację z Kędzierzynem-Koźle .
Rok później otwarto linię do Pilszcza ( 325 ) .
W 1991 i 1992 roku zamknięta została linia do Pilszcza ( odpowiednio dla ruchu pasażerskiego i towarowego ) .
W 1997 zamknięto linię do Kędzierzyna , a w 2000 roku zawieszono ruch pasażerski na linii Głubczyce – Racibórz ( od tego czasu odbywał się na niej wyłącznie ruch towarowy , jednak ten został zawieszony w 2013 roku ) .
Ruch towarowy został ponownie wznowiony 5 grudnia 2016 na życzenie spółki Ecco Rail , ładującej na stacji w Baborowie zboże z przeznaczeniem do transportu do polskich portów .
