Język bacbijski
Język bacbijski , cowa-tuszyński ( bacb .
`` Bacbi '' , `` Cowa-Tusz '' ) – język należący do nach-dagestańskiej rodziny językowej , którym posługuje się licząca 3 tysiące ludzi wspólnota Bacbów , mieszkających po południowej stronie Kaukazu , w miejscowości Zemo Alwani w Kachetii ( dystrykt Achmeta ) .
W obrębie języka istnieje tylko jeden dialekt .
Język bacbijski nie jest zrozumiały dla pozostałych narodów nachskich , tj .
Czeczenów i Inguszy , pomimo tego , iż stanowi najpewniej kontynuację języka inguskich kolonizatorów , którzy osiedlili się w Tuszetii w wiekach średnich .
Jego druga nazwa jest związana z faktem , iż do XIX w. Bacbowie stanowili jeden z czterech klanów Tuszetii , o nazwie – Cowata .
