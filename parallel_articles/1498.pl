Bacewiczy
Bacewiczy ( ; , `` Bacewiczi '' ) – agromiasteczko na Białorusi , w obwodzie mohylewskim , w rejonie kliczewskim , około 28 km na północ od Bobrujska , nad rzeką Olsą .
W 2009 roku liczyło 730 mieszkańców .
Historia .
Obszerne dobra bacewskie były własnością rodziny Haraburdów herbu Abdank , później przez pewien czas były własnością Radziwiłłów , od których majątek ten w 1696 roku kupili Bacewicze .
Kilkanaście lat później ziemia ta przeszła na własność Stanisława Niezabytowskiego .
W rękach jego rodziny dobra te pozostawały aż do I wojny światowej .
Ostatnim ich właścicielem przed zaborem sowieckim był Karol Niezabytowski ( 1865–1952 ) .
Wieś szlachecka położona była w końcu XVIII wieku w powiecie orszańskim województwa witebskiego .
Według Słownika geograficznego Królestwa Polskiego Bacewicze w 1860 roku liczyły 109 mieszkańców , 12 domów i `` znaczną fabrykę terpentyny '' .
Od 1924 roku jest siedzibą jednego z 12 sielsowietów rejonu kliczewskiego .
W Bacewiczach urodził się m.in .
