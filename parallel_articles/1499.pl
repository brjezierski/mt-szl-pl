Bachorza ( województwo warmińsko-mazurskie )
Bachorza ( niem .
`` Wiesenthal '' ) – osada mazurska w Polsce położona w województwie warmińsko-mazurskim , w powiecie giżyckim , w gminie Ryn , w sołectwie Tros , w pobliżu drogi krajowej nr 59 .
W latach 1975–1998 miejscowość należała administracyjnie do województwa suwalskiego .
