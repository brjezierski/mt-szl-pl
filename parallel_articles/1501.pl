Bacolod
Bacolod – miasto i port na Filipinach , na północno-zachodnim wybrzeżu wyspy Negros , nad Cieśniną Guimaras .
Jest ośrodkiem administracyjnym prowincji Negros Occidental .
W 2010 roku jego populacja liczyła 511 820 mieszkańców .
W mieście rozwinięty przemysł przetwórczy trzciny cukrowej , centrum handlu cukrem i ryżem , uniwersytet ( zał .
w 1941 ) , lotnisko .
