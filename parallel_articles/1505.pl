Bad Honnef
Bad Honnef ( do 1960 `` Honnef '' ) – miasto uzdrowiskowe w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Kolonia , w powiecie Rhein-Sieg .
W 2010 r. liczyło 25 213 mieszkańców .
Gospodarka .
W mieście rozwinął się przemysł chemiczny oraz elektroniczny .
