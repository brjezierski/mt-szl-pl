Bad Kissingen ( stacja kolejowa )
Bad Kissingen ( niem : `` Bahnhof Bad Kissingen '' ) – czołowa stacja kolejowa w Bad Kissingen , w regionie Bawaria , w Niemczech .
Posiada zabytkowy budynek dworca z tzw `` Królewskim salonem '' , jedynym tego typu wciąż istniejącym obiektem w Bawarii .
`` Wojna prusko-austriacka '' z 1866 ze względu na brak połączenia kolejowego ujawniła poważne braki logistyczne w Bad Kissingen , dlatego król Ludwik II Wittelsbach w dniu 9 stycznia 1867 zatwierdził budowę linii kolejowej sieci kolejowej .
Opis .
Początkowo stacja była planowana jako przelotowa , dla pociągów między Gemünden i Schweinfurt oraz Bad Neustadt an der Saale .
Plany te nie zostały zrealizowane i jest stacją czołową .
Jest ona używana do przewozów towarowych i regionalnych pasażerskich na linii Gemünden – Ebenhausen , i pociągów Erfurter Bahn między Hammelburg i Schweinfurt .
Co dwie godziny kursują pociągi Regional-Express do Würzburga .
