Bad Krozingen
Bad Krozingen – miasto uzdrowiskowe w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Fryburg , w regionie Südlicher Oberrhein , w powiecie Breisgau-Hochschwarzwald , siedziba wspólnoty administracyjnej Bad Krozingen .
Leży nad rzeką Neumagen , ok. 15 km od Fryburga Bryzgowijskiego .
Prawa miejskie od 1 września 2005 .
