Bad Liebenzell
Bad Liebenzell – miasto uzdrowiskowe w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Nordschwarzwald , w powiecie Calw , siedziba wspólnoty administracyjnej Bad Liebenzell .
Leży w północnym Schwarzwaldzie , nad rzeką Nagold , ok. 7 km na północ od Calw , przy drodze krajowej B463 .
