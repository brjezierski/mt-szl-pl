Bad Mergentheim
Bad Mergentheim – miasto uzdrowiskowe w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Heilbronn-Franken , w powiecie Main-Tauber-Kreis , siedziba wspólnoty administracyjnej Bad Mergentheim .
Leży nad rzeką Tauber .
Bad Mergentheim zlokalizowane jest ok. 18 km na południowy wschód od miasta Tauberbischofsheim .
Drogi , które przebiegają przez Bad Mergentheim to krajowe B19 , B290 , autostrada A81 , a także linia kolejowa Crailsheim – Lauda-Königshofen .
W latach 1525–1809 miasto Bad Mergentheim było siedzibą władz zakonu krzyżackiego .
