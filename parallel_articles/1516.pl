Bad Schlema
Bad Schlema – uzdrowiskowa część miasta ( `` Ortsteil '' ) Aue-Bad Schlema w Niemczech , w kraju związkowym Saksonia , w powiecie Erzgebirgskreis .
Do 31 grudnia 2018 samodzielna gmina .
Do 29 lutego 2012 w okręgu administracyjnym Chemnitz , do 31 lipca 2008 w powiecie Aue-Schwarzenberg .
Geografia .
Bad Schlema leży w zachodniej części powiatu , pomiędzy Aue i miastem Schneeberg .
