Bad Vilbel
Bad Vilbel − miasto uzdrowiskowe w Niemczech , w kraju związkowym Hesja , w rejencji Darmstadt , w powiecie Wetterau .
Leży na południowym krańcu doliny Wetterau nad rzeką Niddą , graniczy z Frankfurtem nad Menem .
Składa się z dzielnic : Bad Vilbel , Heilsberg , Gronau , Dortelweil oraz Massenheim .
Nad brzegiem Niddy zachowały się ruiny średniowiecznego zamku .
W mieście znajduje się źródło wody mineralnej `` Elisabethen Quelle '' .
