Bad Wildbad
Bad Wildbad ( do 1990 `` Wildbad im Schwarzwald '' ) – miasto uzdrowiskowe w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Nordschwarzwald , w powiecie Calw , siedziba wspólnoty administracyjnej Bad Wildbad .
Leży w północnym Schwarzwaldzie , ok. 13 km na północny zachód od Calw .
