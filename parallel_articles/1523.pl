Baden-Baden ( 1920 )
Baden-Baden - pierwotnie szkuner , żaglowiec ( rotorowiec ) z napędem rotorami Flettnera poprzednio zwany `` Buckau '' .
Historia i rejsy .
W pierwszy rejs handlowy wyruszył w lutym 1925 roku z ładunkiem drewna z W. M. Gdańsk do Szkocji .
Po zmianie nazwy popłynął w rejs do USA .
Przybycie statku do Nowego Jorku 9 maja 1926 przyciągnęło rzeszę gapiów .
Baden-Baden został zniszczony przez sztorm na Karaibach w 1931 .
W tym czasie rotory były już zdemontowane .
