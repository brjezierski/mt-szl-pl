Badiraguato
Badiraguato – miasto w północno-wschodniej części meksykańskiego stanu Sinaloa , położone w odległości około 150 kilometrów od wybrzeża Pacyfiku nad Zatoką Kalifornijską , na północ od stolicy stanu Culiacán Rosales .
Badiraguato leży w dolinie rzeki o tej samej nazwie , meandrującej u podnóża Sierra Madre Occidental .
Miasteczko w 2005 roku liczyło 3562 mieszkańców .
Cieszy się złą sławą handlarzy narkotyków .
Jest miejscem urodzenia baronów narkotykowych – Rafaela Caro Quintero i Joaquín Guzmán Loera .
