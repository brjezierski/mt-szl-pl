Baesweiler
Baesweiler – miasto w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Kolonia , w regionie miejskim Akwizgran .
W 2010 roku miasto liczyło 27 898 osób .
Jest trzecim co do wielkości miastem powiatu .
Do 1974 r. miasto miało połączenie trolejbusowe z Akwizgranem .
