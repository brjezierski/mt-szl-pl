Język bagulalski
Język bagulalski ( nazwy własne `` Kwantl hekua '' , `` Bagolal '' ) – jeden z niewielkich języków kaukaskich , używany przez Bagulalów .
Należy do języków andyjskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Bagulalski jest blisko spokrewniony z językiem czamalalskim oraz tindyjskim .
Dane mówiące o liczbie osób posługujących się tym językiem są bardzo rozbieżne i niepewne , według statystyk w 1926 r. języka tego używało ok. 3 tys .
