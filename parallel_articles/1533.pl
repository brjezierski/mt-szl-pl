Bahamy
Bahamy ( ) , Wspólnota Bahamów ( ang .
`` Commonwealth of The Bahamas '' ) – państwo wyspiarskie na Oceanie Atlantyckim .
Geografia .
Wyspy archipelagu obejmują 13 880 km² i wznoszą się niewiele ponad poziom morza .
Długość linii brzegowej wynosi 3542 km .
Najwyższy punkt archipelagu leży na wysokości 63 m n.p.m .
( Mount Alvernia na Cat Island ) .
Spośród około 700 wysp tylko 26 jest zamieszkanych .
Liczba ludności wynosi 382,6 tys .
osób ( 2014 ) .
Powierzchnia wysp ma charakter równinny , a wzdłuż niektórych fragmentów wybrzeża znajdują się słone bagna porośnięte namorzynem .
Średnia temperatura wynosi +27 °C w lecie i +21 °C w zimie , zaś roczna suma opadów deszczu przekracza 1000 mm .
Klimat .
Bahamy często nawiedzają tropikalne burze i huragany .
