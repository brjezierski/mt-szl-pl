Bahawalpur ( księstwo )
Bahawalpur – dawne księstwo muzułmańskie znajdujące się na obszarze dzisiejszego Pakistanu .
Państwo obejmowało obszar 45,911 km² , jego stolicą było Bahawalpur .
Państwo utworzono w 1690 roku .
22 lutego 1833 Bahawalpur przyjął protektorat brytyjski .
7 października 1947 wszedł w skład Pakistanu jako autonomiczne księstwo .
14 października 1955 księstwo zlikwidowano i włączono do prowincji Pendżab .
