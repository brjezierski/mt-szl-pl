`` Al-Bahrajn '' ) , oficjalnie Królestwo Bahrajnu ( arab .
مملكة البحرين , trb .
`` Mamlakat al-Bahrajn '' ) – państwo o ustroju monarchicznym , położone w Azji , w Zatoce Perskiej , na archipelagu między Katarem a północno-wschodnim wybrzeżem Arabii Saudyjskiej .
W 1971 roku uzyskał niepodległość .
Od lat 30 .
XX wieku wydobywa się tam ropę naftową , a uzyskane dochody inwestuje na miejscu .
Od 2002 roku panujący Bahrajnem używa tytułu `` król '' , wcześniej posługiwał się tytułem `` emir '' .
Pochodzenie nazwy .
Po arabsku `` bahrajn '' ( بحران ' ) oznacza “ dwa morza ” .
Nazwa może wskazywać na dwa źródła wody , słodką wodę źródlaną i słoną morską , lub też na części Zatoki Perskiej na północ i południe od wyspy .
Geografia .
Państwo Bahrajnu zajmuje wyspę Al-Bahrajn i kilkanaście mniejszych przybrzeżnych wysp i wysepek .
Bahrajn jest krajem wybitnie nizinnym , pokrytym pustyniami i miejscami słonymi bagnami .
Najwyższy punkt : Dżabal ad-Duchan 122 m n.p.m. , a linia brzegowa liczy 161 km .
Państwo to leży w strefie klimatu zwrotnikowego suchego , cechującym się typowymi warunkami pustynnymi .
