Baia Mare
Baia Mare ( nazwa oznacza po polsku „ Wielką Kopalnię ” , węg .
`` Nagybánya '' , niem .
`` Frauenbach '' lub `` Neustadtl '' ) – stolica i największe miasto okręgu Marmarosz ( obecnie często błędnie uznawanym za część Siedmiogrodu ) w Rumunii .
Miasto w 2002 roku liczyło 137 921 mieszkańców .
Historia .
W czasie epoki brązu tereny wokół Baia Mare stanowiły królestwo Traków , z których później wywodzili się Dakowie .
Były też częścią dużego państwa Daków stworzonego przez Burebistę .
Najstarsze wzmianki na temat miasta pochodzą z 1142 roku , kiedy to węgierski król Geza II sprowadził tutaj niemieckich osadników .
Nazwa tej osady brzmiała po niemiecku Frauenbach ( węg .
Asszonypataka , łac .
Rivulus Dominarum ) i oznaczała „ Rzekę Kobiet ” .
