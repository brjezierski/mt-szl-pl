Baje ( wł .
`` Baia '' , łac .
`` Baiae '' ) – miasto nad Zatoką Neapolitańską w rzymskiej Kampanii , modne uzdrowisko ze wspaniałymi willami , m.in .
Cezara i Nerona .
Miejsce śmierci cesarza Hadriana .
Większa część starożytnego miasta znalazła się w wyniku aktywności wulkanicznej pod wodami Zatoki Neapolitańskiej .
