Baiersbronn
Baiersbronn – miejscowość i gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Nordschwarzwald , w powiecie Freudenstadt .
Leży w Schwarzwaldzie , nad rzeką Murg , ok. 5 km na północny zachód od Freudenstadt , przy drodze krajowej B462 .
W miejscowości znajduje się skocznia narciarska Große Ruhestein o rozmiarze HS 90 , gdzie rozgrywane są zawody Pucharu Kontynentalnego w kombinacji norweskiej i w skokach narciarskich pań .
Z Baiersbronn pochodzi Svenja Würth , niemiecka skoczkini narciarska , mistrzyni świata .
