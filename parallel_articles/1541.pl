Baja
Baja lub bay ' a ( arab .
بَيْعَة = „ sprzedaż ” , „ transakcja handlowa ” ) – islamska przysięga wierności składana kalifowi przez muzułmanów .
Odbierający przysięgę kalif zobowiązywał się do przestrzegania przepisów prawa muzułmańskiego , natomiast muzułmanin składający ją uznawał autorytet kalifa i przysięgał mu posłuszeństwo .
Muzułmanin składający przysięgę symbolicznie dotykał otwartej dłoni kalifa .
Jej złożeniu często towarzyszyła wielka uroczystość .
Baję złożyło Mahometowi 1400 muzułmanów w Al-Hudajbiji .
Mirza Gulam Ahmad , założyciel islamskiego ruchu reformatorskiego ahmadijja , formalnie rozpoczął swoje panowanie nad islamską gminą od odebrania baji .
W czasach przedislamskich pojęcie to oznaczało zakończenie transakcji handlowej wyrażone przez potrząśnięcie dłonią .
