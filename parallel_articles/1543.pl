Bawaria
Bawaria , Wolny Kraj Bawaria , Wolne Państwo Bawaria ( niem .
`` Bayern '' , `` Freistaat Bayern '' ) – największy pod względem powierzchni i najlepiej rozwinięty gospodarczo kraj związkowy Niemiec .
Geografia .
Bawaria graniczy z Austrią , Czechami i Szwajcarią ( od strony Jeziora Bodeńskiego ) oraz krajami związkowymi Badenią-Wirtembergią , Hesją , Turyngią i Saksonią .
Rzeki : Altmühl , Alz , Amper , Dunaj , Soława Frankońska , Iller , Inn , Isen , Izara , Lech , Men , Mindel , Naab , Paar , Pegnitz , Regen , Regnitz , Rott , Vils , Wertach
Kanały : Ren-Men-Dunaj
Jeziora : Ammersee , Chiemsee , Starnberger See
Ważniejsze wzniesienia : Nebelhorn ( 2224 m ) , Zugspitze ( 2963 m ) , Watzmann ( 2714 m ) , Grosser Arber ( 1457 m )
Historia Bawarii .
W VI wieku obecna Bawaria wchodziła w obszar państwa plemiennego Bawarów , ludu germańskiego z korzeniami alemańskimi , turyngijskimi , frankońskimi i ostrogockimi .
Część północno-wschodnia obszaru dzisiejszej Bawarii była zasiedlona przez Słowian ( zob .
Bavaria Slavica ) .
Bawarowie stworzyli własne księstwo , później zależne od państwa Franków .
Pierwszym znanym władcą był Garibald ( Heribald ) I , panujący w latach 548 ( ? ) –590 .
Od 788 do 907 r. Bawaria znajdowała się w granicach państwa Franków .
Następnie władzę przejęła miejscowa dynastia Luitpoldingów ( 907–947 i krótko 983–985 ) .
Bawaria znalazła się wkrótce w granicach Świętego Cesarstwa Rzymskiego , przy czym zajmowała południową część dzisiejszego kraju związkowego .
Na północy regionu istniały mniejsze kraje , w tym Burgrabstwo Norymbergi , księstwo biskupie Bambergu i Wolne Miasto Norymberga , a w XIV wieku sięgało tu władztwo Czech ( Czeski Palatynat ) .
W 1070 r. tron bawarski został przez króla niemieckiego Henryka IV oddany sprzymierzonym z nim Welfom ( 1070–1139 i 1156–1180 ) .
Od 1180 do 1918 Bawaria była rządzona przez członków dynastii Wittelsbachów , którzy od 1623 r. byli elektorami .
W 1806 stała się królestwem .
W 1806 do Bawarii włączono Norymbergę , a w 1815 Palatynat Reński .
Od 1818 r. stała się monarchią konstytucyjną ( na podstawie Aktu Konstytucyjnego , `` Verfassungs-Urkunde '' ) .
Panujący od 1825 roku Ludwik I Wittelsbach zmienił oficjalny zapis nazwy państwa z `` Baiern '' na `` Bayern '' ; litera „ y ” miała symbolizować tradycję starożytną w przeciwieństwie do potocznej „ i ” .
18 stycznia 1828 r. Bawaria i Wirtembergia utworzyły Bawarsko-Wirtemberski Związek Celny ( `` Bayerisch-Würtembergischer Zollverein '' ) , a sześć lat później przystąpiły do Niemieckiego Związku Celnego .
W 1871 r. Bawaria została włączona do Cesarstwa Niemieckiego , z zachowaniem odrębności ( poczta , polityka zagraniczna , kolej i armia w czasie pokoju ) , które zlikwidowano dopiero w 1918 r. W tym samym roku doszło do krótkotrwałego powołania Bawarskiej Republiki Rad .
W 1920 do Bawarii przyłączono Coburg , od 1572 związany z pomniejszymi księstwami saskimi .
W 1949 r. Bawaria weszła w skład Republiki Federalnej Niemiec .
We wrześniu 2006 roku , tuż przed pielgrzymką do Niemiec , papież Benedykt XVI użył określenia `` Państwo Wielka Bawaria '' .
Podział administracyjny .
Bawaria podzielona jest na siedem rejencji ( `` Regierungsbezirke '' ) :
Mieszkańcy każdego z tych regionów posiadają nieco odrębną kulturę i rozpoznawalny akcent językowy .
Równolegle do władz rejencji działają władze okręgu ( niem .
`` Bezirk '' ) .
Niższym szczeblem podziału jest podział na 71 powiatów i 25 miast na prawach powiatu .
Polityka .
W Bawarii istnieje ruch na rzecz niepodległości : Partia Bawarska otrzymała w wyborach do landtagu w 2013 poparcie 2,3 % .
Sondaż przeprowadzony przez YouGov w 2017 roku wskazuje , że niezależność od Niemiec popiera 1/3 Bawarczyków .
Gospodarka .
Bawaria należy do najbogatszych regionów współczesnych Niemiec .
Wysoko rozwinięty jest tam przemysł elektroniczny , maszynowy ( w tym samochodowy ) , lotniczy , włókienniczy , rafineryjny ( ropa dostarczana jest rurociągami z portów Morza Śródziemnego ) .
Produkt krajowy brutto Bawarii wynosi łącznie 544 mld USD i było większe niż PKB całej Polski aż do końca 2017 roku ( PKB Polski w 2018 roku wyniosło 614,2 mld ) .
Nazwa znanego producenta samochodów – BMW – „ Bawarskie Zakłady Silnikowe ” ( `` Bayerische Motoren-Werke '' ) .
Inne znane przedsiębiorstwa z Bawarii : Siemens , Audi ( samochody osobowe ) , MAN ( autobusy i samochody ciężarowe ) , MTU ( silniki lotnicze i czołgowe ) , Airbus Group ( europejski koncern militarny i lotniczy ) , Telefónica Deutschland ( operator komórkowy ) , HypoVereinsbank , Munich Re ( jedna z dwóch największych firm reasekuracyjnych na świecie ) , Allianz SE ( grupa ubezpieczeniowo-finansowa ) , Sky Deutschland ( niemiecka płatna telewizja ) , ProSiebenSat.1 Media ( koncern mediowy ) .
Bawaria nazywana jest również doliną krzemową Niemiec .
Wysoko rozwinięte i wydajne jest też bawarskie rolnictwo .
Bawaria szczyci się największą w Niemczech produkcją mleka , serów i wyrobów mleczarskich .
Na terenie Bawarii znajdują się trzy elektrownie atomowe : Isar w Essenbach , Grafenrheinfeld , Gundremmingen .
W miejscowości Garching bei München ( 15 km na północ od Monachium ) znajduje się reaktor badawczy .
Kultura .
Słynny festiwal piwa `` Oktoberfest '' odbywa się corocznie od 1810 roku we wrześniu w Monachium i jest największym festiwalem tego typu na świecie .
Początek imprezy jest wyznaczany na dwa tygodnie przed pierwszą niedzielą października , a festiwal trwa dwa tygodnie ( dokładnie od piątku do niedzieli , czyli 17 dni ) .
Religia .
Bawaria jest krajem o tradycji katolickiej .
Katolicy stanowią w Bawarii 54,4 % ludności ( tj .
6 mln 796 tys . ) .
Obserwuje się jednak duży spadek wiernych przyznających się do Kościoła katolickiego ( w 1970 70,4 % , w 2011 56,3 % ) .
W Altötting znajduje się jedno z najważniejszych w Europie sanktuariów kultu maryjnego .
Z miejscowości Marktl w południowo-wschodniej Bawarii pochodzi Benedykt XVI .
