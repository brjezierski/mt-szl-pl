Bagnet
Bagnet ( franc .
`` baïonette '' od miasta `` Bayonne '' ) – rodzaj kolnej broni białej mocowanej u wylotu lufy broni strzeleckiej ( np .
karabinu ) .
Po nałożeniu na broń palną bagnet stanowi jej przedłużenie umożliwiające efektywniejsze wykorzystanie jej w walce wręcz .
Historia .
Nazwa wywodzi się od nazwy francuskiego miasta Bayonne znanego niegdyś z wytwarzania najlepszych bagnetów w Europie .
Pierwsze bagnety – szpuntowe , wprowadzone do użytku w połowie XVII wieku – były zwykłymi ostrzami obsadzanymi we wnętrzu lufy muszkietu .
Stosowane były nie tylko w czasie wojen , ale także podczas polowań , np .
na dziki .
Powtórne naładowanie muszkietu zabierało dużo czasu i mimo wypracowania technik kontrmarszu , po oddaniu strzału żołnierze byli niemal bezbronni , w szczególności wobec ataku szybkiej jazdy wyposażonej w długie kopie , wobec których krótka broń boczna była nieskuteczna .
Muszkieterzy wymagali więc ochrony pikinierów , uzbrojonych w długie piki , którymi zasłaniali się przed szarżą kawalerii .
