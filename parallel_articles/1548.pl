Baku ( stacja kolejowa )
Baku – stacja kolejowa w Baku , w Azerbejdżanie .
Jest największą stacją kolejową w kraju .
Znajduje się tu 8 peronów .
Dworzec połączony jest z siecią metra – w pobliżu znajdują się stacje 28 May i Cəfər Cabbarlı .
Stacja oferuje połączenia krajowe i międzynarodowe ( Moskwa , Tbilisi , Charków , Rostów nad Donem )
Stacja jest przystosowana do obsługi kolei wysokich prędkości , od 2019 roku można stąd dojechać taką koleją do Gandży .
Rozstaw szyn torów na stacji wynosi 1520 mm .
Historia .
W 1880 roku otwarto stację w Baku , przy okazji otwarcia pierwszej , 20-kilometrowej linii kolejowej Baku – Sabunçu – Surakhani .
Linia ta , której wybudowanie zlecono Towarzystwu Kolejowemu Poti – Tbilisi , służyła transportowi ropy naftowej wydobywanej na Półwyspie Apszerońskim do rafinerii w Baku .
29 lutego 1924 roku ( 21 dni po otwarciu sieci tramwajowej w Baku ) , Rada Miejska Baku podjęła decyzję o konieczności rozpoczęcia budowy podmiejskiej kolejki elektrycznej .
Władze partyjne były również zainteresowane elektryfikacją linii Baku – Sabunçu .
Tę inwestycję rozpoczęto w marcu 1925 roku , a 26 lipca 1926 roku pierwsze pociągi elektryczne zaczęły kursować z baku na pola naftowe Sabunçu i Surakhani .
Budynek dworca został ukończony w roku 1884 , wybudowano go w stylu mauretańskim .
Wysoka wieża została dobudowana w trakcie rozbudowy dworca w 1977 roku .
W latach 2015 – 2017 dworzec przeszedł gruntowny remont , w wyniku którego m.in .
powstały nowe kasy biletowe oraz stoiska gastronomiczne i usługowe ; budynek dostosowano także do potrzeb osób niepełnosprawnych .
