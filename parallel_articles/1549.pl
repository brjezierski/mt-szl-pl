Piłka ( skała )
Piłka – skała w miejscowości Dubie w województwie małopolskim , w powiecie krakowskim , w gminie Krzeszowice .
Wznosi się u wylotu Doliny Racławki i Doliny Szklarki w grupie Skał nad Boiskiem .
Pod względem geograficznym jest to obszar Wyżyny Olkuskiej .
Skały nad Boiskiem to przygotowany w 2016 roku staraniem fundacji Wspinka nowy rejon wspinaczkowy .
Wznoszą się na otwartym terenie , nad boiskiem sportowym i mają wystawę południowo-zachodnią .
Obok , oprócz boiska sportowego znajduje się plac zabaw dla dzieci , niewielki parking oraz tablice ze skałoplanami .
Do skał w górnej części urwiska wykonano schodki .
Piłka znajduje się w środkowej części Skał nad Boiskiem .
Po jej lewej stronie ( patrząc z boiska ) są skały Murawa i Jupiter , a po prawej kolejno : Trybuna , Bramka i Słupek .
Jak dotąd nie wszystkie jeszcze skały zostały przygotowane do wspinaczki .
Na Piłce brak asekuracji , nie ma też jej skałoplanu .
