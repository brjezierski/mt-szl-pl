Balanga ( film )
Balanga – film produkcji polskiej ; debiut reżyserski Łukasza Wylężałka z 1993 roku .
Według krytyka filmowego Tadeusza Sobolewskiego `` Balanga '' to polskie `` Miasteczko Twin Peaks '' .
Fabuła .
W peryferyjnej dzielnicy wielkiego miasta pewnej nocy zostaje znaleziony martwy nauczyciel szkoły zawodowej .
Podczas dochodzenia ujawnianie są okoliczności , które wyjaśniają śmierć pedagoga .
Zmarły wiecznie był skacowany , znęcał się nad swoimi uczniami fizycznie i psychicznie oraz nienawidził swoich podopiecznych i zawodu .
Feralnej nocy podczas spaceru w parku spotkał dwóch swoich uczniów : „ Świętego ” i „ Snajpera ” .
