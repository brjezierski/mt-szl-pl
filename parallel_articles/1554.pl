Baligród
Baligród – wieś w Polsce położona w województwie podkarpackim , w powiecie leskim , w gminie Baligród .
Leży w dolinie rzeki Hoczewki .
Jest siedzibą gminy Baligród .
Dawniej miasto ; uzyskał lokację miejską w 1634 , zdegradowany w 1919 .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Baligród .
W latach 1975–1998 miejscowość położona była w województwie krośnieńskim .
Od 1 stycznia 1999 do 1 stycznia 2002 w powiecie bieszczadzkim .
Ośrodek usługowo-gospodarczy dla okolicy .
O dawnym miejskim charakterze świadczy zachowany układ urbanistyczny z obszernym prostokątnym rynkiem .
Przez wieś przebiega droga wojewódzka nr 893 .
Miejscowość jest siedzibą rzymskokatolickiej parafii Niepokalanego Poczęcia NMP , należącej do dekanatu Lesko w archidiecezji przemyskiej .
