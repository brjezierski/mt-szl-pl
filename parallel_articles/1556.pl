Ballarat
Ballarat – miasto w Australii w stanie Wiktoria , położone 100 km na północny zachód od Melbourne , jest największym śródlądowym ( tj .
nie leżącym nad morzem ) miastem stanu Wiktoria .
Miasto zamieszkane przez 104 355 osób ( 2016 ) .
Ballarat było centrum gorączki złota w latach 50 .
XIX wieku ( 1851 ) oraz widownią zbrojnego powstania zbieraczy złota .
Obecnie mieści się tu skansen będący repliką Ballarat z okresu gorączki złota ( Sovereign Hill ) .
Nadal czynna jest również kopalnia złota w Ballarat operowana przez firmę Castlemaine Gold ( CGT ) .
W mieście rozwinął się przemysł metalowy , włókienniczy , odzieżowy , spożywczy , chemiczny oraz meblarski .
Miasto położone jest na wysokości ok. 550 m n.p.m .
co czyni jego klimat chłodniejszym o 3 °C od klimatu Melbourne .
Ballarat jest siedzibą biskupstwa Kościoła rzymskokatolickiego .
Prime Ministers Avenue ( Aleja Premierów ) – w Ogrodach Botanicznych w Ballarat znajduje się jedyna w Australii kolekcja wykonanych z brązu popiersi australijskich premierów .
