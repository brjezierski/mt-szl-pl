Ballycastle
Ballycastle ( irl .
`` Baile an Chaistil '' ) – miasto w Irlandii Północnej , siedziba dystryktu Moyle w hrabstwie Antrim oraz okręgu wyborczego North Antrim .
Leży na brzegu Kanału Północnego , naprzeciw wyspy Rathlin i półwyspu Kintyre .
Graniczy z AONB `` Antrim Coast and Glens '' .
Liczy 5089 mieszkańców ( 2001 ) , w tym 77.7 % to katolicy i 20.5 % protestanci .
Transport .
Miasto posiada połączenie promowe z wyspą Rathlin , operowane przez Rathlin Island Ferry Company ( poprzednio przez szkockie przedsiębiorstwo Caledonian MacBrayne ) .
Do czerwca 2002 istniało również połączenie do Campbeltown w Szkocji .
Stacja kolei wąskotorowej funkcjonowała w mieście w latach 1880-1950 , obsługując kursy do Ballymoney .
