Ballymena ( stacja kolejowa )
Ballymena ( ang : `` Ballymena railway station '' ) – stacja kolejowa w Ballymena , w hrabstwie Antrim , w Irlandii Północnej , w Wielkiej Brytanii .
Znajduje się on niedaleko centrum miasta Ballymena na Galgorm Road , i jest połączona z lokalnym dworcem autobusowym .
Znajduje się na linii Belfast-Darry , między Antrim i Cullybackey .
Stacja jest obsługiwana przez Translink i wszystkie pociągi są obsługiwane przez Northern Ireland Railways .
Stację Ballymena otwarto 4 grudnia 1855 .
W pewnym momencie było kilka innych stacji na terenie Ballymena , jednak jedyną , która przetrwała do dnia dzisiejszego jest Cullybackey .
