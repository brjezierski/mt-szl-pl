Balsamka szerokozarodnikowa
Balsamka szerokozarodnikowa ( `` Balsamia platyspora '' Berk . )
– gatunek grzybów należący do rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Balsamia , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1844 r. Miles Joseph Berkeley w ziemi w Anglii .
Synonim : `` Balsamia vulgaris '' var .
`` platyspora '' ( Berk . )
