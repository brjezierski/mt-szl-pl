Balsthal
Balsthal – gmina w Szwajcarii , w kantonie Solura , w okręgu Thal .
Gmina została po raz pierwszy wspomniana w dokumentach w 968 roku jako `` Palcivallis '' .
W 1255 została wspomniana jako `` Balcetal . ''
Demografia .
W Balsthal mieszka 6117 osób .
W 2008 roku 28,9 % populacji gminy stanowiły osoby urodzone poza Szwajcarią .
W 2000 roku 84,8 % ( 4728 osób ) populacji mówiło w języku niemieckim , 2,9 % ( 163 osoby ) w języku albańskim , a 2,7 % ( 153 osoby ) w języku włoskim .
