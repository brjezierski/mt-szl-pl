Balıkesir
Balıkesir – miasto w Turcji .
Ośrodek administracyjny prowincji Balıkesir .
Według danych na rok 2000 miasto zamieszkiwało 215 436 osób , a według danych na rok 2004 całą prowincję ok. 1 103 000 osób .
Gęstość zaludnienia w prowincji wynosiła ok. 77 osób na km² .
W mieście rozwinął się przemysł włókienniczy , rafineryjny oraz spożywczy .
W pobliżu miasta znajdują się kopalnie srebra oraz port lotniczy Balıkesir .
