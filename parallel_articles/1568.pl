Balnica ( przystanek kolejowy )
Balnica ( ; ; dosł .
`` Szpital '' ) – przystanek kolejowy w miejscowości Żłobin , w rejonie żłobińskim , w obwodzie homelskim , na Białorusi .
Położony jest na linii Orsza - Mohylew - Żłobin .
Perony znajdują się ok. 300 m w linii prostej od siebie , po obu stronach budynków żłobińskiego szpitala .
