Bamako
Bamako – stolica i największe miasto Mali .
Położone w południowo-zachodniej części kraju , nad rzeką Niger , w pobliżu granicy z Gwineą .
Współrzędne geograficzne Bamako to 12°39′N i 8°0′W .
W 2010 r. miasto liczyło 1 793 730 mieszkańców .
Tempo wzrostu liczby mieszkańców jest obecnie najwyższe w Afryce i szóste na świecie .
Bamako jest centrum administracyjnym kraju , ważnym portem rzecznym i istotnym ośrodkiem handlowym dla całego regionu .
Posiada rangę odrębnej jednostki administracyjnej ( dystrykt stołeczny Bamako ) i podzielone jest na sześć gmin , którymi kierują wybieralni burmistrzowie .
Obecnym burmistrzem dystryktu stołecznego Bamako jest Adama Sangaré .
Położenie .
Bamako leży na terenach zalewowych rzeki Niger , co hamuje rozwój miasta wzdłuż brzegów rzeki i jej dopływów .
Teren , na którym miasto jest usytuowane , jest stosunkowo płaski .
