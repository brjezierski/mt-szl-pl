Historia kolei .
Kolej , począwszy od 4. dziesięciolecia XIX w. , znalazła zastosowanie jako środek transportu w Europie .
Rozpowszechnienie kolei spowodowało olbrzymi przewrót w transporcie , w życiu gospodarczym , kulturalnym i politycznym .
Kolej przeobrażała charakter gospodarczy państw , jak np .
w Szwajcarii , która dzięki kolejom z kraju pasterskiego i rolniczego stała się krajem wybitnie przemysłowym .
Wzmacniała spoistość państw przez zbliżanie odległych granicznych terenów do centrum , np .
Kolej Transsyberyjska , która na początku XX w. połączyła wschodnią Syberię i Mandżurię z Rosją europejską .
Wzmacniała też zdolności militarne i obronne państwa , umożliwiając szybką mobilizację i przerzucanie wojsk oraz łatwe ich zaopatrzenie .
