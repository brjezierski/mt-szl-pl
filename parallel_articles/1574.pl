Bandundu ( prowincja )
Bandundu – prowincja w Demokratycznej Republice Konga ( w latach 1971-1997 znanej jako `` Zair '' ) o powierzchni 295.658 km² , zamieszkana w 2012 roku przez 8,4 mln osób .
Stolicą jest miasto Bandundu .
Na mocy konstytucji z 2006 roku prowincja Bandundu ma zostać podzielona na trzy mniejsze prowincje : Kwilu , Kwango i Mai-Ndombe .
