Bandung
Bandung – miasto w Indonezji , ośrodek administracyjny prowincji Jawa Zachodnia , liczy ponad 2,4 mln mieszkańców , a razem z przedmieściami około 6 milionów .
Geografia .
Położone na wyżynach zachodniej Jawy na wysokości 715 m n.p.m .
nad rzeką Cikapundung .
Jest położone w Kotlinie Bandung , otoczonej przez wulkany , wśród których niektóre z nich są nadal aktywne .
Miasto leży w obszarze aktywnym sejsmicznie .
Historia .
Miasto zostało założone w 1810 r. przez osadników holenderskich .
