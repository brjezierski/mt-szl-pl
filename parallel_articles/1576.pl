Bungee
`` Bungee jumping '' ( skok na `` bungee '' ) to skakanie z dużej wysokości na długiej elastycznej linie wykonanej zwykle z około tysiąca gumowych włókien .
Pierwszymi skoczkami byli rdzenni mieszkańcy wysp Pentecost ( Nowe Hebrydy ) na Pacyfiku .
Młodzi mężczyźni skakali z bambusowych wież , zabezpieczeni lianami przywiązanymi do kostek , żeby udowodnić swoją męskość oraz , według wierzeń , zapewnić plemieniu urodzaj .
W kwietniu 1979 roku wykonano skoki w Wielkiej Brytanii z mostu Clifton Suspension Bridge w Bristolu .
Wydarzenie to uznaje się za początek `` bungee jumpingu '' , gdyż był to pierwszy skok oddany poza wyspą Pentecost .
Warianty .
Wystrzeliwanie .
Amator wrażeń `` wystrzeliwany '' jest na wysokość kilkudziesięciu metrów za pomocą urządzenia w rodzaju procy : pomiędzy dwoma wieżami rozpięte są dwie liny .
Są one połączeniem gum bungee i normalnych lin napinanych systemem naciągu .
Trzeba tylko wpiąć się do uprzęży i zwolnić wyczep .
Bungee-run .
W specjalnym korytarzu trzeba naciągnąć linę bungee do granic swoich możliwości .
Nadmuchiwany korytarz i kask zapewniają bezpieczne zakończenie zabawy .
Sprzęt .
Guma bungee zbudowana jest z wielu setek cienkich lateksowych gumek a w renomowanych firmach dodatkowo wplatają taśmę alpinistyczną zapobiegającą nadmiernemu rozciągnięciu się .
Stosuje się trzy grubości gum : cienka dla wagi skoczka od 40 do 65 kg , średnia od 65-90 kg , gruba od 90 do 120 kg .
Można skakać w kilka osób naraz .
