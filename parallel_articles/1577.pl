18 lutego 1965 roku miasto stało się stolicą niepodległej Gambii .
24 kwietnia 1973 roku starą kolonialną nazwę Bathurst zmieniono na Bandżul , co oznacza bambus .
W mieście znajduje się główny port oceaniczny kraju , a w jego pobliżu główny port lotniczy kraju , Banjul International Airport , którego 3600-metrowa droga startowa była w latach 1990-2002 jednym z awaryjnych miejsc lądowania dla amerykańskich promów kosmicznych .
Miasto ma połączenie promowe z Barra na północnym brzegu rzeki Gambia .
Przez przeprawę prowadzi międzynarodowa droga samochodowa do północnej części Senegalu .
