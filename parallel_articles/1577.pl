Bandżul
Bandżul ( ang .
`` Banjul '' ) – stolica i czwarte co do wielkości miasto Gambii ( największe jest pobliskie miasto Serrekunda ) , usytuowane na wyspie St. Mary ( albo `` Banjul Island '' ) u ujścia rzeki Gambii do Oceanu Atlantyckiego .
