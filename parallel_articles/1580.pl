Bangkok
Bangkok ( taj .
Krung Thep lub Krung Thep Mahanakhon ) – stolica i największe miasto Tajlandii , położona przy ujściu rzeki Menam do Zatoki Tajlandzkiej .
Liczba ludności wynosi ponad 5,6 miliona , a aglomeracja ma około 15 milionów .
Połączenie azjatyckich tradycji z nowoczesną infrastrukturą sprawiło , że jest to jeden z najpopularniejszych na świecie celów turystycznych .
W 2018 miasto odwiedziło 21,98 mln turystów – Bangkok był najczęściej odwiedzanym miastem na świecie .
Pełna nazwa .
Pełna nazwa Bangkoku w języku tajskim brzmi : กรุงเทพมหานคร อมรรัตนโกสินทร์ มหินทรายุธยามหาดิลก ภพนพรัตน์ ราชธานีบุรีรมย์ อุดมราชนิเวศน์ มหาสถาน อมรพิมาน อวตารสถิต สักกะทัตติยะ วิษณุกรรมประสิทธิ์ , czyli `` Krung Thep Mahanakhon Amon Rattanakosin Mahinthara Ayuthaya Mahadilok Phop Noppharat Ratchathani Burirom Udomratchaniwet Mahasathan Amon Piman Awatan Sathit Sakkathattiya Witsanukam Prasit '' ( ) , co w tłumaczeniu oznacza :
`` Miasto aniołów , wielkie miasto [ i ] rezydencja świętego klejnotu Indry [ Szmaragdowego Buddy ] , niezdobyte miasto Boga , wielka stolica świata , ozdobiona dziewięcioma bezcennymi kamieniami szlachetnymi , pełne ogromnych pałaców królewskich , równającym niebiańskiemu domowi odrodzonego Boga ; miasto , podarowane przez Indrę i zbudowane przez Wiszwakarmana . ''
Do 1764 dzisiejsza metropolia była niewielką wioską zwaną Bang Makok ( Wieś Drzewek Oliwnych ) .
Po zniszczeniu przez wojska birmańskie w tymże roku dawnej stolicy Syjamu ( obecnej Tajlandii ) , Ajutthaji , zbudowano tu obóz warowny .
Podczas swojej koronacji w 1782 Rama I nadał swej stolicy nazwę składającą się z 43 sylab .
Przez następne lata dodano do tej nazwy jeszcze 21 sylab .
Nazwa ta obecnie znajduje się w Księdze Rekordów Guinnessa jako najdłuższa nazwa geograficzna na świecie .
Klimat .
Według Światowej Organizacji Meteorologicznej Bangkok jest najgorętszą stolicą świata .
Średnia roczna temperatura przekracza 28 °C .
Najcieplejszym miesiącem jest kwiecień z przeciętną temperaturą dzienną od 31 °C .
Najchłodniejszy miesiąc w Bangkoku to grudzień ze średnią temperaturą 26 °C .
Zabudowa .
Bangkok jest miastem zdecentralizowanym .
