Bangladesz
Bangladesz ( beng .
বাংলাদেশ ) , Ludowa Republika Bangladeszu ( beng .
গণপ্রজাতন্ত্রী বাংলাদেশ , `` Gonoprodźatontri Bangladesz '' ) – państwo w Azji Południowej , położone nad Zatoką Bengalską i graniczące z zachodu , północy i wschodu z Indiami , a na południowym wschodzie z Mjanmą .
Jest jednym z najgęściej zaludnionych krajów świata i zarazem jednym z najbiedniejszych państw Azji .
Stolicą Bangladeszu jest Dhaka ( dawna Dakka ) .
Geografia .
Bangladesz jest krajem wybitnie nizinnym , gdzie występują aluwialne niziny , plejstoceńskie terasy i trzeciorzędowe wyżyny ( Góry Czatgańskie ) .
Leży on w strefie wilgotnego klimatu zwrotnikowego , kształtowanego przez wiatry monsunowe .
Charakterystyczne są wysokie przez cały rok temperatury i występowanie dwóch pór roku : deszczowej i suchej .
Kraj ten ma gęstą sieć rzeczną a największym układem rzecznym jest Brahmaputra–Jamuna .
Naturalna szata roślinna została wyniszczona na potrzeby rolnictwa .
Występują niewielkie obszary lasów monsunowych .
Klęski żywiołowe .
Częste są tajfuny , zwłaszcza latem i jesienią .
