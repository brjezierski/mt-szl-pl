Bangi
Bangi ( fr .
`` Bangui '' ) – stolica i największe miasto Republiki Środkowoafrykańskiej , położone w południowej części kraju na zachodnim brzegu rzeki Ubangi .
Zostało założone w 1889 roku jako fort wojsk francuskich .
Bangi było również miejscem działalności rebeliantów i zniszczeń spowodowanych przez bunty na tle politycznym .
Przez te wydarzenia miasto w 1996 roku zostało ogłoszone jednym z najbardziej niebezpiecznych miast na świecie .
Historia .
Założone w 1889 roku .
Od 1905 roku było stolicą francuskiej kolonii Ubangi-Szari .
