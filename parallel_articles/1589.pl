Baník Ostrawa
Baník Ostrawa ( czes .
Baník Ostrava ) – czeski klub piłkarski z Ostrawy .
Występuje w czeskiej ekstraklasie .
Historia .
Początki ( 1922-1937 ) .
Klub został założony 8 września 1922 w dzielnicy Śląska Ostrawa jako `` SK Slezská Ostrava '' , kiedy w miejscowej restauracji `` U Dubu '' w Śląskiej Ostrawie podpisano akt założycielski .
Jego sygnatariuszami byli w większości ubodzy pracownicy kopalni węgla kamiennego ( czes .
`` baník '' = górnik ) , zamieszkujący lokalne osiedle górnicze Kamenec .
Wśród grona założycielskiego klubu byli Karel Aniol , Arnošt Haberkiewicz , Petr Křižák , František Mruzek i Jaroslav Horák .
Rejestracja klubu nastąpiła 14 października , a Aniol został pierwszym zarządcą klubu .
Historyczne stroje klubu były w czerwono-białe pasy , lecz od kwietnia 1923 piłkarze występowali w białych koszulkach i niebieskich spodenkach , które obowiązują do dziś .
Debiutancki mecz miał miejsce 4 marca 1923 , kiedy SK Slezská Ostrava zmierzył się z rezerwami innego lokalnego zespołu – Slovana Ostrawa .
Klub na początku swojego istnienia borykał się z problemami finansowymi i wynajmował boiska od zamożniejszych stowarzyszeń sportowych .
Jesienią 1925 na Kamencu powstało pierwsze boisko sportowe należące do klubu – nie spełniało ono jednak regulaminowych wymagań .
W 1934 działacze doprowadzili do wynajęcia działki na `` Stará střelnice '' od miejscowego przemysłowca , hrabiego Wilczka – w ciągu lata tego samego roku powstało tam boisko sportowe ( tzw .
stadion „ Stará střelnice ” ) .
W budowie brali udział miejscowi robotnicy i górnicy , w wolnym czasie dobrowolnie pomagając przy powstawaniu stadionu .
