Baranowicze ( rejon postawski )
Baranowicze ( ; ) – wieś na Białorusi , w obwodzie witebskim , w rejonie postawskim , w sielsowiecie Juńki .
Historia .
W czasach zaborów w gminie Postawy , powiatu dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie wileńskim , w powiecie duniłowickim , od 1926 w powiecie dziśnieńskim , w gminie Postawy .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 146 osób , wszystkie były wyznania prawosławnego i zadeklarowały białoruską przynależność narodową .
Było tu 31 budynków mieszkalnych .
W 1931 w 34 domach zamieszkiwało 138 osób .
Wierni należeli do parafii prawosławnej w Postawach .
Miejscowość podlegała pod Sąd Grodzki w Postawach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Postawach .
Po agresji ZSRR na Polskę w 1939 roku wieś znalazła się w granicach BSRR .
W latach 1941–1944 była pod okupacją niemiecką .
Następnie leżała w BSRR .
Od 1991 roku w Republice Białorusi .
