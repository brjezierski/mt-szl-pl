Barbados
Barbados – państwo wyspiarskie w Ameryce Środkowej , zajmujące wyspę o tej samej nazwie , położoną w archipelagu Małych Antyli , na Oceanie Atlantyckim .
Najbardziej na wschód wysunięta wyspa Karaibów .
Powierzchnia kraju wynosi 430 km² , a liczba ludności – 294 560 ( 2020 ) .
Stolicą i największym miastem jest Bridgetown .
Językiem urzędowym jest angielski .
Od 1627 do 1966 roku Barbados był kolonią Wielkiej Brytanii .
W unii personalnej z nią ( `` Commonwealth realm '' ) pozostawał do 30 listopada 2021 r. , kiedy to stał się republiką .
Głową państwa została wybrana przez parlament prezydent – Sandra Mason .
Państwo jest jednym z zamożniejszych i najbardziej rozwiniętych we wschodniej części Karaibów .
Gospodarka kraju tradycyjnie opierała się na uprawie i przetwórstwie trzciny cukrowej , od końca XX wieku istotną rolę odgrywają także turystyka i przemysł lekki .
Geografia .
Wyspa , w przeciwieństwie do innych wysp archipelagu , nie ma budowy wulkanicznej , a wapienną .
Początek wyspie dały rafy koralowe , które otaczają jej wody .
Występują pagórki , z których najwyższy ( Mount Hillaby ) nie przekracza 340 m n.p.m .
Brak wyraźnych wód śródlądowych , ale istnieją rozwinięte zjawiska krasowe .
Wyspa otoczona ze wszystkich stron wodami Oceanu Atlantyckiego charakteryzuje się tropikalnym klimatem podrównikowym .
W lecie wyspę nawiedzają cyklony .
Pochodzenie nazwy .
Nazwa wyspy pochodzi z języka portugalskiego .
Portugalczycy zwrócili uwagę na miejscową odmianę figowca , tworzącego dużą siatkę korzeni , przypominającą brodę .
Drzewa te nazwano „ drzewami brodatymi ” ( `` os barbados '' ) , skąd przeszła nazwa na całą wyspę .
Historia .
Barbados zasiedlili 4000 lat temu Indianie .
Wyspę odkryli dla Europejczyków na początku XVI wieku portugalscy podróżnicy , jednak jej nie skolonizowali .
W 1627 roku na wyspie wylądowali angielscy koloniści , w pobliżu obecnej miejscowości Holetown , ale na Barbadosie mieszkali również Holendrzy .
Ci ostatni w 1637 roku przywieźli z Brazylii trzcinę cukrową i założono na wyspie pierwsze plantacje tej rośliny .
