Barbara Liskov
Barbara Liskov ( ur .
jako Barbara Jane Huberman 7 listopada 1939 w Los Angeles ) – amerykańska informatyczka .
Jest profesorem na Massachusetts Institute of Technology .
Była jedną z pierwszych kobiet , które uzyskały doktorat z informatyki w Stanach Zjednoczonych i jest zdobywczynią nagrody Turinga .
Sformułowała zasadę podstawienia .
Wczesne życie i edukacja .
Liskov urodziła się 7 listopada 1939 w Los Angeles , jako najstarsza z czwórki dzieci Jane ( z domu Dickhoff ) i Mosesa Hubermana .
W 1961 roku ukończyła studia licencjackie na kierunkach matematyki i fizyki na Uniwersytecie Kalifornijskim w Berkeley .
Następnie aplikowała na studia magisterskie na kierunku matematyki na Berkeley i Princeton .
W tamtym czasie Princeton nie przyjmował kobiet na ten kierunek .
Została przyjęta na Berkeley , jednak zdecydowała się przeprowadzić do Bostonu , by zacząć pracę w Mitre Corporation .
To tam zainteresowała się komputerami i programowaniem .
W Mitre przepracowała rok , a następnie objęła stanowisko programistki na Harvardzie , gdzie pracowała nad tłumaczeniami języków .
Wkrótce zdecydowała się powrócić do nauki i ponownie zaaplikowała na Berkeley , a także na Stanford i Harvard .
W 1968 po ukończeniu Uniwersytetu Stanford została jedną z pierwszych kobiet w Stanach Zjednoczonych , która uzyskała doktorat z informatyki .
Na Stanfordzie pracowała z Johnem McCarthym nad sztuczną inteligencją .
Tematem jej pracy doktorskiej był program komputerowy symulujący gry końcowe w szachach .
Kariera zawodowa .
Po ukończeniu studiów , Liskov wróciła do Mitre jako pracownik badawczy .
W Mitre prowadziła wiele znaczących projektów , takich jak system operacyjny Venus , mały , tani system operacyjny pozwalający na jednoczesne użytkowanie przez wielu użytkowników ; zaprojektowanie i implementacja języka CLU ; Argus , pierwszy język wysokiego poziomu , który wspierał implementację programów rozproszonych i zademonstrował technikę obietnic ; Thor , obiektowy system baz danych .
Razem z Jeannette Wing sformułowała definicję podtypowania , znaną jako zasada podstawienia Liskov .
Kieruje grupą metodologii programowania MIT , a obecnie skupia się na badaniu problemu bizantyjskich generałów i obliczeń rozproszonych .
Uznania i nagrody .
Członkini Amerykańskiej Akademii Sztuk i Nauk , Association for Computing Machinery oraz National Academy of Engineering .
W 2002 magazyn Discover wyróżnił Liskov jako jedną z 50. najważniejszych kobiet w nauce .
W 2004 została nagrodzona medalem Johna von Neumanna za fundamentalny wkład do rozwoju języków programowania , metodologii programowania oraz systemów rozproszonych .
19 listopada 2005 wraz z Donaldem Knuthem otrzymała honorowy doktorat uczelni Politechniki Federalnej w Zurychu .
W 2018 roku otrzymała tytuł doktora honoris causa na Universidad Politécnica de Madrid .
W marcu 2009 roku otrzymała nagrodę Turinga za rok 2008 .
Nagrodę umotywowano wkładem w projektowanie języków programowania oraz metodologii oprogramowania , która doprowadziła do rozwoju programowania obiektowego .
Liskov rozwinęła dwa języki programowania – CLU i Argus w latach 70 i 80 .
W 2012 została włączona do National Inventors Hall of Fame .
Barbara Liskov jest autorką trzech książek i ponad stu prac naukowych .
