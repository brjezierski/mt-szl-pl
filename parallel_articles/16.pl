.ax
.ax – krajowa domena internetowa najwyższego poziomu przypisana do Wysp Alandzkich aktywna od 2006 roku .
Wcześniej strony z były rejestrowane pod fińską domeną .aland.fi .
Historia .
17 lutego 2006 fiński parlament zaakceptował modyfikację ustaw regulujących fińskie nazwy domeny , które obejmowały domenę .ax .
Podczas trzyletniego okresu , strony z domeny aland.fi zostaną stopniowo wycofane ; żadne nowe rejestracje pod aland.fi nie są już możliwe .
