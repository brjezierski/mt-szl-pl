Barbórka ( film )
Barbórka z cyklu Święta polskie – polski film fabularny z 2005 roku w reżyserii Macieja Pieprzycy , na podstawie scenariusza Bartosza Kurowskiego .
Fabuła .
Aby uświetnić obchody Barbórki , dyrektor kopalni ( Leszek Piskorz ) zaprasza na akademię gwiazdora telenoweli - Jakuba Skowierskiego ( Marcin Dorociński ) .
Powitać aktora ma Basia ( Iwona Sitkowska ) , pracownica kopalni .
W ten sposób spotykają się ze sobą dwa różne światy : pozytywnie nastawieni warszawiacy i tradycyjni Ślązacy .
