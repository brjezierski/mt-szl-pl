Barchlin
Barchlin – wieś w Polsce położona w województwie wielkopolskim , w powiecie wolsztyńskim , w gminie Przemęt .
Podstawowe funkcje wsi : rolnicza i przemysłowa .
Położenie .
Miejscowość Barchlin położona jest w południowo-wschodnim pasie wysoczyznowym , na skraju dwóch dużych rejonów : Wysoczyzny Leszczyńskiej i Pradoliny Warszawsko-Berlińskiej .
Administracyjnie należy do gminy Przemęt , która zlokalizowana jest na granicy Pojezierza Lubuskiego i Pojezierza Wielkopolskiego .
