Barisza
Barisza ( ) – miejscowość w Syrii , w muhafazie Idlib .
Znajdujące się w jej pobliżu starożytne ruiny należą do tzw .
martwych miast .
W spisie powszechnym z 2004 roku miejscowość liczyła 1143 mieszkańców .
W nocy z 26 na 27 października 2019 we wsi Barisza w amerykańskich sił specjalnych Delta Force zginął Abu Bakr al-Baghdadi , przywódca organizacji terrorystycznej Państwo Islamskie .
Zapędzony do ślepego tunelu terrorysta popełnił samobójstwo wysadzając się w powietrze .
