Gospodarka .
W mieście rozwinął się przemysł maszynowy , środków transportu , elektrotechniczny , chemiczny , włókienniczy , spożywczy oraz materiałów budowlanych .
Historia .
Miejscowość założona została w 1738 roku jako forteca rosyjska chroniąca przed koczownikami pobliską kopalnię srebra .
Prawa miejskie otrzymała w 1771 roku .
