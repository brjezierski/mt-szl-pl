Barnsley
Barnsley – miasto w Wielkiej Brytanii , w Anglii , w hrabstwie South Yorkshire , w dystrykcie metropolitalnym Barnsley , u podnóża Gór Pennińskich , nad rzeką Dearne , w odległości ok. 20 kilometrów od Sheffield .
W przeszłości miasto było znane z przetwórstwa lnu i rozwiniętego handlu płótnem .
Od lat 50 .
XIX wieku w otaczających je wsiach , zwłaszcza na wschodzie , rozpoczęto budowę licznych kopalń węgla kamiennego .
Wydobycie węgla było głównym przemysłem miasta aż do późnych lat 50 .
