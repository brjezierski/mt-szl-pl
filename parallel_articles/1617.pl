Barranquilla
Barranquilla – gmina , miasto i stolica departamentu Atlántico , w północnej Kolumbii .
W 2018 roku miasto liczyło 1 274 250 mieszkańców ( 4. miejsce w Kolumbii po Bogocie , Medellínie i Cali ) , a gęstość zaludnienia wyniosła 8274,35 os./km2 .
Miasto znajduje się na zachodnim brzegu rzeki Magdalena , około 7,5 km od jej ujścia do Morza Karaibskiego .
Gmina graniczy od południa z Galapą oraz Soledad , a od północy z Puerto Colombią .
Barranquilla była gospodarzem igrzysk Ameryki Środkowej i Karaibów w 1946 roku oraz w 2018 roku i ma być gospodarzem w 2027 roku .
Etymologia .
Nazwa Barranquilla nawiązuje do wąwozów , które istniały na terenie przylegającym do rzeki Magdaleny , gdzie powstało miasto .
Przydomki .
W 1849 roku prezydent Nowej Granady Tomás Cipriano de Mosquera podczas odwiedzin , nadał miastu przydomek `` La Arenosa '' ( Piaszczysta ) '' . ''
W 1921 roku prezydent Kolumbii Marco Fidel Suárez nazwał miasto Złotą Bramą Republiki ( `` La '' `` Pórtico Dorado de la República '' ) w uznaniu jego znaczenia gospodarczego .
W 1946 roku , otwierając V Igrzyska Ameryki Środkowej i Karaibów , prezydent Luis Mariano Ospina Pérez nazwał miasto Złotą Bramą Kolumbii ( `` La Puerta de Oro de Colombia '' ) .
