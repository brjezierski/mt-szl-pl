Barreiro ( Maio )
Barreiro - miasto na wyspie Maio , w Republice Zielonego Przylądka .
W 2010 roku liczyło 535 mieszkańców , co czyniło je najmniejszym miastem pod względem liczby mieszkańców na wyspie .
Miejscowość położona w południowej części wyspy , około 4 km na wschód od centrum administracyjnego concelho Maio , miasta Porto Inglês .
