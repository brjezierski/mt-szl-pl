Barry ( pies z Treblinki )
Barry , w niektórych źródłach występujący pod imieniem Bari – pies służący w niemieckich nazistowskich obozach zagłady w Sobiborze i Treblince .
W pamięci ocalałych z Treblinki nierozerwalnie związany z osobą Kurta Franza ; razem tworzyli tandem będący postrachem więźniów .
Wygląd .
Barry był mieszańcem z dominującymi cechami bernardyna .
Miał umaszczenie białe z czarnymi plamami .
Był psem o bardzo dużych rozmiarach , świadkowie wspominali , że wielkością przypominał cielaka .
Według esesmana Ericha Bauera skacząc był w stanie powalić na ziemię dorosłego człowieka .
