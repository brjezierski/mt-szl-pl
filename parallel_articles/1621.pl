Barsbüttel
Barsbüttel ( dolnoniem .
`` Barsbüddel '' ) – gmina w Niemczech w kraju związkowym Szlezwik-Holsztyn w powiecie Stormarn .
Liczy ok. 12 375 mieszkańców ( 31 grudnia 2008 ) .
Dzielnice .
Barsbüttel , Stellau , Stemwarde i Willingshausen .
Zabytki .
Jednym z bardziej znanych zabytków jest `` Villa Lunugala '' z 1907 , wybudowana przez Wilhelma Antona `` Tonio '' Riedemanna .
Gospodarka .
Gmina jest ośrodkiem usługowym i produkcyjnym ( meble , art .
budowlane ) .
