Barssja oregoneńska
Barssja oregoneńska ( `` Balsamia oregonensis '' ( Gilkey ) K. Hansen & amp ; X.H .
Wang ) – gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Balsamia , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisała w roku 1925 Helen Margaret Gilkey , nadając mu nazwę `` Barssia orogenensis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2019 r. K. Hansen i X.H .
Wang .
W pracy M.A .
Chmiel ma nazwę barssja oregońska , która jest spolszczeniem nazwy naukowej `` Barssia oregonensis '' .
Jest niespójna z aktualną nazwą naukową .
Gatunki należące do rodzaju `` Balsamia '' mają polską nazwę balsamka .
