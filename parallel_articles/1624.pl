Bartosz Paprocki
Bartłomiej ( Bartosz ) Paprocki , Bartolomej Paprocký herbu Jastrzębiec ( ur .
ok. 1540 w Paprockiej Woli , ( `` obecnie nazywanej Starą Wolą '' ) koło Sierpca , zm .
27 grudnia 1614 we Lwowie ) – podczaszy dobrzyński , heraldyk polski i czeski ( nazywany ojcem heraldyki polskiej i czeskiej ) , autor wielu herbarzy , pisarz , historyk , poeta i tłumacz , wydał liczne prace heraldyczne , wśród nich „ `` Herby rycerstwa polskiego '' ” .
Działał w Polsce , a na przełomie XVI i XVII wieku w Czechach i na Morawach .
Założył wieś Bartoszowiny w województwie świętokrzyskim .
Rodzina .
Urodził się w rodzinie Paprockich , w Paprockiej Woli w parafii Sierpc .
Był synem Jędrzeja Paprockiego , herbu Jastrzębiec i Elżbiety z Jeżewskich .
Miał dwóch braci – Rosłańca , późniejszego proboszcza płońskiego i Wojciecha .
Studiował na Akademii Krakowskiej , po czym przebywał u zamożnych krewnych m.in .
na dworze Modliszewskiego , Gorajskiego , Piotra Gorajskiego .
Po śmierci rodziców , powrócił do domu i zajął się gospodarowaniem pozostałymi po nich dwiema małymi wsiami – Paprocką Wolą i Głogołami .
Ożenił się z bogatą , dwukrotną wdową , Jadwigą , z domu Kossobudzką , córką Mikołaja , kasztelana sierpeckiego , dziedziczką Krajkowa i połowy Lipy koło Raciąża .
Po jej śmierci wyjechał do Czech .
Twórczość .
Początkowo pisał wiersze , jednak wkrótce poświęcił się pisarstwu historycznemu oraz heraldyce .
Dzięki dziełom poświęconym szlachcie polskiej jak „ `` Gniazdo cnoty '' ” ( 1578 ) oraz „ `` Herby rycerstwa polskiego '' ” ( 1584 ) pogłębił i przyczynił się do utrwalenia wiedzy o genealogii polskich rodów szlacheckich .
Wyjechał do Warszawy na dwór Andrzeja Taranowskiego , sekretarza królewskiego , któremu towarzyszył w jego drugim poselstwie do Stambułu .
Po powrocie do kraju dowiedział się o śmierci żony , która nastąpiła w 1572 r. Osiadłszy na swych włościach otrzymał godność podczaszego ziemi dobrzyńskiej .
W 1577 r. wziął udział w gdańskiej wyprawie Stefana Batorego .
W latach 80 .
XVI w. związał się z rodem Zborowskich .
Stał się stronnikiem Habsburgów , popierając austriackiego księcia Maksymiliana w nieudanych staraniach o tron polski po śmierci Batorego ( 1586 ) .
Po zwycięstwie zwolenników Zygmunta III w bitwie pod Byczyną ( 1588 ) zmuszony został opuścić kraj i udał się na emigrację do Czech i na Morawy , gdzie spędził 22 lata .
Przebywał na dworze biskupa ołomunieckiego – Stanisława Pawłowskiego w Kromieryżu .
Przez ten czas opanował język czeski i oddał się pracy pisarskiej .
Wkrótce uzyskał także czeski indygenat .
Był autorem historii Moraw , herbarzy szlachty morawskiej i czeskiej oraz tłumaczem wierszy Jana Kochanowskiego na język czeski .
Był także autorem wielu utworów poezji okolicznościowej , satyr , panegiryków i pamfletów .
Wartością dzieł Paprockiego jest korzystanie z licznych źródeł polskich i czeskich , które nie zachowały się do czasów dzisiejszych .
W 1610 roku u kresu życia powrócił z ogarniętych wojnami religijnymi Czech do Polski .
Pozbawiony środków do życia mieszkał u cystersów w Wąchocku oraz Lądzie .
Zmarł nagle we Lwowie w 1614 r. i został pochowany w krypcie u franciszkanów ( franciszkański `` klasztor i kościół św .
Krzyża we Lwowie przy placu Castrum przekształcono w r. 1785 na umieszczenie głównej szkoły normalnej , urzędu mierniczego , teatru miejskiego i sali redutowej '' ) .
Uwagi .
Jest w Polsce jeszcze jedna wieś związana z nazwiskiem Bartłomieja Paprockiego , którą w roku 1599 obejmuje Stanisław Paprocki , po Bartłomieju , swoim bracie stryjecznym .
