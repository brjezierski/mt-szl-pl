Bartoszyce
Bartoszyce ( dawniej `` Barsztyn '' , ) – miasto w północnej Polsce , w województwie warmińsko-mazurskim , siedziba powiatu bartoszyckiego .
Miasto leży na Nizinie Sępopolskiej w dolnym biegu rzeki Łyny .
Pod względem historycznym Bartoszyce leżą w Prusach Dolnych , na obszarze średniowiecznego pruskiego terytorium plemiennego Barcji , kilkanaście kilometrów na północ od granic Warmii .
Bartoszyce to miasto o wielowiekowej historii i tradycji , łączące w sobie wpływy polskie , niemieckie i bałtyckie .
W latach 1954–1972 miasto było siedzibą władz gromady Bartoszyce , choć do niej nie należało .
W latach 1975–1998 Bartoszyce administracyjnie należały do województwa olsztyńskiego .
Dzisiaj to ważny węzeł komunikacyjny oraz ośrodek administracyjny , usługowy i kulturalny województwa .
1 stycznia 2018 Bartoszyce liczyły 23 810 mieszkańców .
Położenie i lokalizacja .
Bartoszyce położone są w północnej części województwa warmińsko-mazurskiego po obu stronach rzeki Łyny .
Geograficznie znajdują się w mezoregionie Równina Sępopolska , która jest częścią makroregionu Nizina Staropruska .
