Bartłomiej Misiewicz
Bartłomiej Sebastian Misiewicz ( ur .
27 marca 1990 w Warszawie ) – polski polityk oraz przedsiębiorca , w latach 2015–2017 szef gabinetu politycznego i rzecznik prasowy Ministerstwa Obrony Narodowej , jeden z bliskich współpracowników Antoniego Macierewicza .
Życiorys .
W 2009 ukończył XII Liceum Ogólnokształcące im .
Henryka Sienkiewicza w Warszawie .
Podejmował studia licencjackie na Społecznej Akademii Nauk w Łodzi , na Uniwersytecie Kardynała Stefana Wyszyńskiego w Warszawie , a w 2015 w Wyższej Szkole Kultury Społecznej i Medialnej w Toruniu .
W 2018 na tej ostatniej uczelni obronił licencjat pt .
`` Szczyt NATO 2016 w Warszawie i jego wpływ na bezpieczeństwo Polski '' .
Następnie rozpoczął tam studia magisterskie na kierunku politologia , a także studia podyplomowe na Akademii Sztuki Wojennej .
Działał w Klubie „ Gazety Polskiej ” .
W wieku 17 lat zgłosił się do pracy w biurze poselskim Antoniego Macierewicza .
W 2010 został członkiem Prawa i Sprawiedliwości , a następnie z rekomendacji Antoniego Macierewicza został mianowany szefem biura zespołu parlamentarnego ds .
zbadania przyczyn katastrofy smoleńskiej .
W 2012 pełnił funkcję sekretarza Zarządu Okręgowego PiS w okręgu piotrkowskim .
Z nominacji Jarosława Kaczyńskiego został członkiem Rady Politycznej PiS .
W latach 2012–2014 był koordynatorem ds .
struktur wykonawczych PiS , w latach 2013–2014 pełnił funkcję pełnomocnika powiatowego PiS w powiecie radomszczańskim , a od 2014 był członkiem Krajowej Komisji Rewizyjnej PiS .
W 2015 bez powodzenia startował do Sejmu w wyborach parlamentarnych z 4. miejsca na liście w okręgu piotrkowskim , zdobywając 1992 głosy .
Po wyborczej wygranej Prawa i Sprawiedliwości , 16 listopada 2015 został szefem gabinetu politycznego oraz rzecznikiem prasowym Ministerstwa Obrony Narodowej .
Przed objęciem stanowisk w MON przez kilka miesięcy pracował w jednej z aptek w podwarszawskich Łomiankach .
Funkcję rzecznika prasowego MON przestał pełnić w lutym 2017 .
1 kwietnia został asystentem politycznym Antoniego Macierewicza .
Był zatrudniony w MON do 14 kwietnia , a jego kompetencje oraz zasługi zostały uhonorowane przez MON pamiątkową monetą .
10 kwietnia 2017 został pełnomocnikiem zarządu państwowej Polskiej Grupy Zbrojeniowej ( PGZ ) ds .
komunikacji .
12 kwietnia spółka rozwiązała z nim umowę o pracę na jego wniosek .
