Bashkim Fino
Bashkim Muhamet Fino ( ur .
12 października 1962 w Gjirokastrze , zm .
29 marca 2021 w Tiranie ) – albański polityk , deputowany , minister w różnych resortach , w 1997 premier Albanii .
Życiorys .
W 1986 ukończył studia ekonomiczne na Uniwersytecie Tirańskim , kształcił się następnie z zakresu zarządzania w administracji samorządowej we Włoszech , Grecji i Finlandii .
Pracował w administracji lokalnej rodzinnej miejscowości , od 1990 był dyrektorem centrum informatyzacji gospodarczej .
W latach 1992–1996 sprawował urząd burmistrza rodzinnej miejscowości .
Działacz postkomunistycznej Socjalistycznej Partii Albanii , w 1997 dołączył do prezydium partii .
Od 11 marca do 24 lipca 1997 sprawował urząd premiera ; prezydent Sali Berisha powierzył mu kierowanie rządem jedności narodowej , powołanym w okresie kryzysu związanego z wydarzeniami tzw .
albańskiej rewolucji piramidowej .
Wchodził następnie w skład socjalistycznych gabinetów jako wicepremier i minister do spraw samorządu terytorialnego ( 1997–1998 ) , minister do spraw samorządu terytorialnego ( 1999–2001 ) , minister robót publicznych i turystyki ( 2001–2002 ) oraz minister do spraw terytorialnych i turystyki ( 2004–2005 ) .
Wybierany na posła do Zgromadzenia Albanii , po raz ostatni w wyborach w 2017 .
Miał kandydować również w wyborach w 2021 ; zmarł jednak 29 marca tegoż roku na skutek COVID-19 .
