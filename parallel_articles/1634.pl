Basidiobolus ranarum
Basidiobolus ranarum Eidam – gatunek grzybów z typu sprzężniaków ( `` Zygomycota '' ) .
Grzyb mikroskopijny , saprotrof i pasożyt ludzi i zwierząt .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Basidiobolus , Basidiobolaceae , Incertae sedis , Basidiobolales , Incertae sedis , Zygomycota , Fungi '' .
Po raz pierwszy opisał go w 1886 r. Michael Emil Eduard Eidam na odchodach żaby trawnej ( `` Rana temporaria '' ) i żaby `` Rana oxyrrhina '' w Niemczech .
Synonimy :
Występowanie i siedlisko .
`` Basidiobolus ranarum '' występuje na wszystkich kontynentach poza Antarktydą .
W Polsce jego występowanie po raz pierwszy podano w 1967 r. na ziemi , w dorosłych postaciach muchówek z rodziny `` Limaniidae '' i `` Tipulidae '' .
Gatunek ten występuje w rozkładającej się roślinności oraz na odchodach gadów i płazów i przypuszczalnie jest normalnym składnikiem flory jelitowej tych zwierząt .
Normalnie jest saprotrofem , ale może też być pasożytem .
