Podstawkodrzewek błękitnawy
Podstawkodrzewek błękitnawy ( `` Basidiodendron caesiocinereum '' ( Höhn .
& amp ; Litsch . )
Luck-Allen ) – gatunek grzybów z rzędu uszakowców ( `` Auriculariales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Incertae sedis , Auriculariales , Auriculariomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1908 r. Franz von Höhnel i Viktor Litschauer nadając mu nazwę `` Corticium caesicinereum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Luck-Allen w 1963 r .
Synonimy :
Nazwę polską zaproponował w 2003 r. Władysław Wojewoda , wcześniej ( w 1977 r. ) używał nazwy suchodrzewek błękitnawy .
Morfologia .
Rozpostarty i całą powierzchnią przylegający do podłoża , bardzo cienki , woskowaty , o barwie od białej do jasnoszarej z niebieskim odcieniem .
