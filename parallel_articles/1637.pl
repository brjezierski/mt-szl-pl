Basingstoke
Basingstoke – miasto w Wielkiej Brytanii ( południowa Anglia ) , w północno-wschodniej części hrabstwa Hampshire , nad rzeką Loddon .
Basingstoke położone jest 77 km na południowy zachód od Londynu i 48 km na północ od Southampton .
W 2001 roku miasto liczyło 90 171 mieszkańców .
Basingstoke jest wspomniana w Domesday Book ( 1086 ) jako `` Basingstoches '' .
Z Basingstoke pochodzi Gabriella Wilde , brytyjska modelka i aktorka .
