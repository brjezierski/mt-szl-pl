Słownictwo .
Wskutek kontaktów z sąsiednimi językami zaczerpnął liczne wyrazy o podłożu hiszpańskim , francuskim czy łacińskim .
Zapożyczenie zwykle wiązało się ze zmianą wynikającą ze specyfiki baskijskiej fonetyki .
Przykładowo : `` lore '' ( „ kwiat ” , od `` florem '' ) , `` errota '' ( „ młyn ” , od `` rotam '' , „ [ młyńskie ] koło ” ) , `` gela '' ( „ pokój ” , od `` cellam '' ) , `` gauza '' ( „ rzecz ” , od `` causa '' ) .
Duża liczba zapożyczeń z łaciny może dowodzić ciągłości kontaktów narodu z mocno zlatynizowanymi grupami ludności miejskiej oraz mieszkańcami obszarów wiejskich .
