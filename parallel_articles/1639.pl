Język baskijski
Język baskijski ( nazwy własne , zależnie od dialektu : , , , ) – język Basków , zamieszkujących kilka prowincji i departamentów na pograniczu Hiszpanii i Francji .
Posługuje się nim dzisiaj ponad milion osób , z których ok. 90 % mieszka w Hiszpanii .
Wyróżnia się dziesięć dialektów języka baskijskiego .
Ze względu na duże różnice pomiędzy nimi powstała ustandaryzowana odmiana języka baskijskiego ( ) , kumulująca cechy największych dialektów języka baskijskiego , oparta zwłaszcza na dialekcie gipuzkoańskim .
Norma ta nauczana jest w szkołach .
Literatura w tym języku powstaje od XVI wieku .
Pierwszą drukowaną książkę w języku baskijskim wydano w 1545 roku .
Jego przynależność genetyczna nie została do tej pory ustalona , ale z pewnością nie jest to język indoeuropejski .
Zwykle uważany jest za język izolowany , lecz istnieją przypuszczenia , że jest on spokrewniony z językami kartwelskimi czy chamickimi , bywa też zaliczany do hipotetycznych makrorodzin – waskońskiej czy dene-kaukaskiej .
W języku baskijskim występują liczne zapożyczenia z łaciny , hiszpańskiego i francuskiego .
