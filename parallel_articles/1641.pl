Bass Generation
Bass Generation – trzeci album studyjny szwedzkiego muzyka Basshuntera , wydany 25 września 2009 roku .
Album promowany był singlami „ Every Morning ” oraz „ I Promised Myself ” .
Album znalazł się między innymi na drugim miejscu nowozelandzkiej listy Top 40 Albums , miejscu szesnastym listy brytyjskiej ( osiągnął status srebra za 60 000 sprzedanych egzemplarzy ) i irlandzkiej .
Album był notowany także na amerykańskich listach : na miejscu piętnastym listy Dance/Electronic Albums oraz na 34. na Heatseekers Albums .
