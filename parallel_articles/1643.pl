W 1998 roku Jonas Altberg zaczął śpiewać ze swoją matką w należącym do niej chórze , przez co zainteresował się śpiewem .
Śpiewał także w zespole rockowym , gdzie nabrał doświadczenia w muzyce eksperymentalnej .
W 2001 roku poznał program komputerowy Fruity Loops , w którym zaczął produkować muzykę .
Po sześciu miesiącach produkcji muzyki , przyjął pseudonim artystyczny „ Basshunter ” .
Wyraz „ Basshunter ” to połączenie dwóch rzeczowników z języka angielskiego „ bass ” i „ hunter ” , oznaczających między innymi „ bas ” i „ łowca ” .
Pseudonim podkreśla jego styl muzyczny .
Jego debiutancki album `` The Bassmachine '' został wydany przez wytwórnię Alex Music 25 sierpnia 2004 roku na płycie CD .
20 grudnia 2005 roku została wydana jego zremasterowana wersja .
W 2006 roku `` The Bassmachine '' oraz `` The Old Shit '' zostały wydane na stronie internetowej Basshuntera .
Publikował swoją muzykę w Internecie ( między innymi na czacie kanału strony internetowej przeznaczonej dla graczy ) , przez kilka lat grał jako DJ w szwedzkich klubach .
20 grudnia 2005 roku ogłosił wydanie singla „ Welcome to Rainbow ” , jednak ostatecznie wydano go 1 kwietnia 2006 roku jako minialbum .
