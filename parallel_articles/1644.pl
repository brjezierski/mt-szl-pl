Język baszkirski
Język baszkirski ( nazwa własna : [ ] ) – język Baszkirów , należący do kipczackiej grupy języków tureckich .
Najbliżej jest spokrewniony z językiem tatarskim , wykazuje jednak względem niego znaczne różnice fonetyczne i gramatyczne .
Językiem baszkirskim posługuje się głównie ludność Baszkortostanu , republiki autonomicznej , wchodzącej w skład Federacji Rosyjskiej , oraz sąsiadujących z Baszkirią obwodów .
Według spisu powszechnego z 1989 r. liczba użytkowników języka wynosiła 1 047 000 osób , natomiast dane szacunkowe za rok 2002 mówią o niespełna 1,4 mln osób .
Zgodnie z danymi spisów powszechnych ponad 20 % Baszkirów uznaje język tatarski za swój język ojczysty .
