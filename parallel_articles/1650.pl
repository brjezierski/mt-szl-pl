( 11739 ) Baton Rouge
( 11739 ) Baton Rouge ( 1998 SG27 ) – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 7,81 lat w średniej odległości 3,93 j.a .
Odkryta 25 września 1998 roku przez amatorskiego astronoma Waltera Cooneya pracującego z obserwatorium Highland Road Park Observatory .
