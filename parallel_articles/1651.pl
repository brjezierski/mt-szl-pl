Szczudłówka piaskowa
Szczudłówka piaskowa ( `` Battarrea phalloides '' ( Dicks . )
Pers . )
– gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Battarea , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1785 r. James Dickson nadając mu nazwę `` Lycoperdon phalloides '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1801 r. Christiaan Hendrik Persoon .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. , Wanda Rudnicka-Jezierska opisywała ten gatunek pod nazwą batarówka słupiakowata .
Morfologia .
Młody owocnik jest kulisty lub jajowaty , o średnicy do 12 cm .
