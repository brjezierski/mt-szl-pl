Battle Beast
Battle Beast – fiński zespół muzyczny wykonujący heavy metal .
Powstał w 2005 roku w Helsinkach .
Historia .
Zespół został założony w 2005 roku w Helsinkach przez szkolnych kolegów gitarzystów Antona Kabanena i Juuso Soinio oraz perkusistę Pyry'ego Vikki .
Pozostali muzycy oryginalne składu dołączyli do zespołu po przesłuchaniach , kolejno wokalistka Nitte Valo , basista Eero Sipilä oraz klawiszowiec Janne Björkroth .
W latach późniejszych grupa zyskała pewną popularność dzięki udziałowi w Wacken Metal Battle 2010 , którego efektem był występ na tymże festiwalu w 2011 roku .
Wkrótce potem zespół został wyróżniony w konkursie Radio Rock Starba organizowanym przez fińską rozgłośnię Radio Rock .
Oddźwięk w mediach umożliwił grupie podpisanie kontraktu z wytwórnią muzyczną Hype Productions .
Debiutancki album zespołu zatytułowany `` Steel '' ukazał się 13 kwietnia 2011 roku .
Nagrania były promowane singlami `` Show Me How to Die '' i `` Enter the Metal World '' , do drugiej z piosenek powstał również teledysk .
Emitowane w fińskich rozgłośniach radiowych nagrania zespoły przysporzyły jej pierwszego sukcesu fonograficznego .
