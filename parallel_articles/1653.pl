Batumi
Batumi ( ) – miasto w południowo-zachodniej Gruzji , na wybrzeżu Morza Czarnego , stolica autonomicznej republiki Adżarii .
Położone jest w pobliżu granicy z Turcją .
Drugie co do wielkości miasto kraju ( po Tbilisi ) .
Batumi jest siedzibą Gruzińskiego Sądu Konstytucyjnego .
W 2016 Batumi liczyło 154,6 tys .
mieszkańców .
Miasto jest dużym ośrodkiem przemysłowym i znanym uzdrowiskiem .
Mieści się tutaj rafineria ropy naftowej ( rurociąg z Baku ) .
Dominuje przemysł maszynowy ( maszyny i urządzenia dla przemysłu spożywczego i naftowego ) , stoczniowy , drzewny , elektrotechniczny oraz spożywczy .
W mieście znajduje się port handlowy ( głównie import ropy naftowej ) i lotniczy , a także wyższa szkoła pedagogiczna .
Stolica Autonomicznej Republiki Adżarii , wchodzącej w skład Republiki Gruzińskiej .
Skład etniczny urozmaicony : Gruzini – chrześcijanie i Gruzini – muzułmanie , przede wszystkim w tej grupie Adżarowie ; Rosjanie , Ormianie i Żydzi .
Historia .
Batumi powstało na miejscu wcześniejszego fortu rzymskiego `` Batys '' , jako gruzińska twierdza o nazwie `` Tamaryscihe '' .
