Bayerisch Eisenstein
Bayerisch Eisenstein ( do 1950 Eisenstein ) – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Bawaria , w regionie Donau-Wald , w powiecie Regen .
Leży w Lesie Bawarskim , około 18 km na północny wschód od miasta Regen , przy B11 , linii kolejowej Deggendorf – Klatovy i granicy Parku Narodowego Lasu Bawarskiego .
Bayerisch Eisenstein leży przy granicy niemiecko-czeskiej .
Zlokalizowane tutaj było przejście graniczne Bayerisch Eisenstein – Železná Ruda ( drogowe i kolejowe ) .
