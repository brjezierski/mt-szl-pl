Półwysep Bałkański
Półwysep Bałkański , skrótowo Bałkany – półwysep w Europie Południowej .
Jego granice wyznacza od zachodu Morze Adriatyckie i Morze Jońskie , od wschodu Morze Czarne i Morze Marmara ( cieśniny Bosfor i Dardanele ) , od południowego wschodu Morze Egejskie .
Północna granica półwyspu ma charakter umowny .
Zgodnie z tradycją przebiega następująco : wzdłuż Dunaju od Morza Czarnego do ujścia Sawy ( rejon Belgradu ) , następnie wzdłuż Sawy do ujścia Kupy ( rejon miasta Sisak ) , następnie wzdłuż Kupy aż do jej źródła ( rejon Osilnicy ) , następnie przekracza pasmo Gorski Kotar przez tzw .
