Nazwa i historia .
Nazwy `` Bałkany '' użył po raz pierwszy włoski pisarz Buonaccorsi Callimarco w 1490 na określenie łańcucha górskiego w Europie Południowej ( Bałkanu ) .
Wywodzi się ono od tureckiego słowa `` balkan '' oznaczającego góry .
Angielski podróżnik , John Moritt , pod koniec XVIII wieku wprowadził ten termin do angielskiej literatury geograficznej .
Od tego czasu zaczęto go odnosić do wyżynno-górzystego terenu leżącego pomiędzy morzami Adriatyckim i Czarnym .
W 1808 niemiecki geograf , , wymyślił nazwę `` Półwysep Bałkański '' , chcąc stworzyć pojęcie równoległe do nazw półwyspów Apenińskiego i Iberyjskiego .
Nazwa `` Bałkany '' nadal funkcjonuje w geografii fizycznej jako określenie łańcucha górskiego położonego na Półwyspie Bałkańskim , na południe od Dunaju , między Żelazną Bramą a Morzem Czarnym .
W języku potocznym termin ten zyskał jednak znacznie szersze znaczenie , wykraczające poza geograficzne pochodzenie .
Bałkany to także region Europy pokrywający się mniej więcej z obszarem Półwyspu Bałkańskiego .
