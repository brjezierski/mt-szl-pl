Bałkarzy
Bałkarzy , Bałkarowie – grupa etniczna zamieszkująca Kaukaz Północny ( a zwłaszcza Kabardo-Bałkarię ) .
Posługują się językiem karaczajsko-bałkarski ( wspólnym z Karaczajami ) , należącym do języków tureckich , stosując pismo cyrylickie .
Od XVIII w. wyznają islam sunnicki ( wcześniej wyznając chrześcijaństwo i animizm ) .
Zajmują się przede wszystkim pasterstwem , częściowo rolnictwem .
W 1944 r. przez władze radzieckie wysiedleni w głąb ZSRR , do Azji Środkowej .
Powrócili w 1957 r .
W 1991 r. powstała Bałkarska Rada Narodowa .
Pod koniec lat 90. ich liczebność szacowana była na 70 000-90 000 .
