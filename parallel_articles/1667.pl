Bałoszyce
Bałoszyce ( niem .
`` Gross Bellschwitz '' ) – osada w Polsce położona w województwie warmińsko-mazurskim , w powiecie iławskim , w gminie Susz , na Pojezierzu Iławskim .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
