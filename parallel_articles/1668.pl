Morze Bałtyckie
Morze Bałtyckie , Bałtyk – morze śródlądowe na szelfie kontynentalnym w północnej Europie .
Połączone z Morzem Północnym przez Cieśniny Duńskie ( Sund , Mały Bełt , Wielki Bełt ) , Kattegat , Skagerrak oraz Limfjorden .
Za zachodnią granicę Bałtyku Właściwego przyjmuje się cieśninę Sund i próg podwodny ciągnący się na głębokości 18–20 m od przylądka Gedser ( wyspa Falster ) do przylądka Darsser Ort ( Darss ) ; na wschód od tej linii znajduje się akwen Bałtyku Zachodniego o powierzchni ok. 8000 km² nazywany przez Niemców także `` Ostsee '' ( Morze Wschodnie ) ; akwen ten obejmuje m.in .
część wód Cieśnin Duńskich ( oprócz Małego i Wielkiego Bełtu ) , a także mniejsze : `` Alsenbelt '' , `` Fehmarnbelt '' , `` Langelandsbælt '' .
Przynależność cieśnin duńskich i Kattegatu do Morza Bałtyckiego jest różnie ujmowana , w zależności od konwencji .
Nazwa Bałtyku , znanego również jako `` Morze Scytyjskie '' , pojawia się po raz pierwszy u niemieckiego kronikarza Adama z Bremy pod koniec XI wieku .
Natomiast na Mapie Morskiej ( Carta Marina ) wydrukowanej w Wenecji w XVI wieku jest z kolei `` Morzem Gockim '' .
Charakterystyka .
Bałtyk nazywany jest morzem śródlądowym północnej Europy , ponieważ ze wszystkich stron jest otoczony lądem , a z Morzem Północnym łączy go jedynie kilka płytkich cieśnin .
Położone jest w północnej strefie klimatu umiarkowanego .
Oba morza leżą na tym samym szelfie kontynentalnym .
Powierzchnia .
Powierzchnia Bałtyku wraz z Kattegatem wynosi ok. 415 266 km² .
Bez Kattegatu Bałtyk zajmuje 392 979 km² .
Powierzchnia zlewni wynosi 1 721 233 km² .
Objętość morza wynosi 21 721 km³ .
