Bałupiany
Bałupiany ( niem .
`` Ballupönen '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie gołdapskim , w gminie Gołdap .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
Podczas akcji germanizacyjnej nazw miejscowych i fizjograficznych ( tzw .
`` chrzty hitlerowskie '' ) utrwalona historycznie nazwa niemiecka `` Ballupönen '' została w 1938 r. zastąpiona przez administrację nazistowską sztuczną formą `` Ballenau '' .
W pobliżu wsi znajduje się grodzisko Jaćwingów .
