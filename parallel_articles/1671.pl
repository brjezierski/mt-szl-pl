Bażanówka
Bażanówka – wieś w Polsce położona w województwie podkarpackim , w powiecie sanockim , w gminie Zarszyn .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krośnieńskiego .
w 2020 Bażanówkę zamieszkiwało 806 mieszkańców .
Historia .
W I poł .
XIX w. Bażanówka należała do Trzecieskich , od których odkupił ją w 1844 Felicjan Laskowski , żonaty z Anielą hr .
Łoś , i posiadał w kolejnych latach .
Po jego śmierci majątek ten odziedziczył w 1875 jego syn , Kazimierz Laskowski ( w 1905 posiadał 212 ha ) .
