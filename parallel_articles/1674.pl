Bearsden
Bearsden – miasto w środkowej Szkocji , w hrabstwie East Dunbartonshire , położone na północnym obrzeżu aglomeracji Glasgow .
W 2011 roku liczyło 27 237 mieszkańców .
Miasto rozwinęło się w XIX wieku , stając się miejscem zamieszkania popularnym wśród właścicieli glasgowskich fabryk .
W 1863 roku otwarta została tu stacja kolejowa .
Współcześnie Bearsden stanowi zamożne przedmieście Glasgow .
Miasto położone jest na szlaku Wału Antonina .
Znajdują się tu pozostałości rzymskich łaźni .
