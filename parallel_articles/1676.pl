Beata Szydło
Beata Maria Szydło z domu Kusińska ( ur .
15 kwietnia 1963 w Oświęcimiu ) – polska polityk , etnograf i samorządowiec , w latach 2015–2017 prezes Rady Ministrów .
W latach 2005–2019 posłanka na Sejm V , VI , VII i VIII kadencji , od 2019 deputowana do Parlamentu Europejskiego IX kadencji , od 2010 wiceprezes Prawa i Sprawiedliwości , w latach 2017–2019 wiceprezes Rady Ministrów oraz przewodnicząca Komitetu Społecznego w pierwszym rządzie Mateusza Morawieckiego .
Życiorys .
Wykształcenie i praca zawodowa .
Jest absolwentką Liceum Ogólnokształcącego im .
Stanisława Konarskiego w Oświęcimiu .
W 1987 ukończyła studia z zakresu etnografii na Wydziale Filozoficzno-Historycznym Uniwersytetu Jagiellońskiego w Krakowie .
W latach 1989–1995 była doktorantką na Wydziale Filozoficzno-Historycznym UJ .
W 1997 ukończyła studia podyplomowe dla menedżerów kultury w Szkole Głównej Handlowej w Warszawie , a w 2001 na Akademii Ekonomicznej w Krakowie zarządzanie samorządem terytorialnym w Unii Europejskiej .
Od 1987 do 1995 pracowała jako asystent w Muzeum Historycznym Miasta Krakowa , następnie kierowała działem merytorycznym w Libiąskim Centrum Kultury .
