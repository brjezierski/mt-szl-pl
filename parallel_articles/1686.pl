Beauvais
Beauvais ( wym . )
– miejscowość i gmina we Francji , w regionie Hauts-de-France , w departamencie Oise .
Według danych na rok 1990 gminę zamieszkiwało 54 190 osób , a gęstość zaludnienia wynosiła 1624 osoby/km² ( wśród 2293 gmin Pikardii Beauvais plasuje się na 3. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 16 . ) .
Zgodnie z francuskim modelem społecznym ok. 43 % mieszkańców miasta zajmuje lokale o czynszu subsydiowanym , a dużym pracodawcą są instytucje publiczne ( przykładowo merostwo Beauvais zatrudnia ok. 130 ogrodników i pracowników zieleni miejskiej ) .
Zabytki .
W Beauvais znajduje się jedna z najsłynniejszych katedr francuskiego gotyku – pod wezwaniem św .
Piotra – o najwyższym sklepieniu gotyckim na świecie , o wysokości 48,5 metrów .
W wyniku trzech kolejnych katastrof podczas jej budowy powstał tylko chór i transept ; nawa , której strop runął w 1573 r. , nie została ukończona .
