Beckum ( Nadrenia Północna-Westfalia )
Beckum – miasto w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Münster , w powiecie Warendorf .
Liczy 36 736 mieszkańców ( 2010 ) .
W mieście rozwinął się przemysł cementowy , maszynowy oraz meblarski .
