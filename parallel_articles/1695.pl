Beer Szewa ( stacja kolejowa )
Beer Szewa Merkaz ( hebr .
: באר שבע מרכז ) – stacja kolejowa w Beer Szewie , w Izraelu .
Jest obsługiwana przez Rakewet Jisra ’ el .
Stacja znajduje się w południowo-wschodniej części Beer Szewy , przy głównym dworcu autobusowym miasta .
Dworzec jest przystosowany dla osób niepełnosprawnych .
Połączenia .
Pociągi z Beer Szewy jadą do Naharijji , Hajfy , Karmi'el i Tel Awiwu .
Kwestie bezpieczeństwa .
Do stacji prowadzi jedno strzeżone wejście , przy którym odbywa się kontrola bagażu , rzeczy osobistych , dokumentów , dokonywana przez uzbrojonych strażników .
Budynek wraz z peronami są monitorowane i kontrolowane .
Wyjście ze stacji odbywa się przez obrotowe bramki .
