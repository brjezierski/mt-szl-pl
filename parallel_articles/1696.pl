Behemoth
Behemoth ( IPA [ ] , hebr .
בהמות dosłownie „ bestie ” ) – polska grupa muzyczna , powstała w październiku 1991 roku w Gdańsku , występująca do 1992 roku pod nazwą Baphomet .
Na początku prezentowała styl muzyczny zaliczany do black metalu , później , od 1994 roku , zbliżony do pagan metalu , szeroko wykorzystując w nagraniach gitarę akustyczną .
Następnie zespół zaczął wykonywać muzykę z rodzaju death metalu , porzucając tematykę pogańską na rzecz szeroko rozumianego okultyzmu oraz mitologii Bliskiego Wschodu .
W początkowym okresie działalności za sprawą gościnnej współpracy z Robertem Fudalim ( Graveland ) grupa była błędnie wiązana z nurtem NSBM .
