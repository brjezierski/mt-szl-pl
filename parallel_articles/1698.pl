Pekin
Pekin ( ; ) – stolica Chińskiej Republiki Ludowej oraz jedno z czterech miast wydzielonych , podlegające bezpośrednio władzom kraju .
Od północy , zachodu i południa graniczy z prowincją Hebei , a od wschodu z miastem wydzielonym Tiencin .
Pekin jest drugim , po Szanghaju , miastem Chińskiej Republiki Ludowej pod względem liczby ludności .
Miasto wydzielone o powierzchni 16 406 km² liczyło w 2020 roku ok. 21,8 mln mieszkańców .
Miasto pełni funkcję największego ośrodka politycznego , oświatowego i kulturalnego kraju .
Wielki ośrodek gospodarczy z rozwiniętym przemysłem maszynowym , samochodowym , taboru kolejowego , elektronicznym , chemicznym , rafineryjnym , spożywczym , włókienniczym .
Krzyżują się tu ważne linie kolejowe i drogi samochodowe .
W mieście znajduje się ambasada RP .
Nazwa .
Nazwa Běijīng znaczy dosłownie „ Północna Stolica ” .
Stolice państw Azji Wschodniej często mają w swojej nazwie wyraz `` stolica '' .
Przykładami mogą być : Nankin , dawna stolica Chin ( znaczy `` Południowa Stolica '' ) , Tokio w Japonii oraz `` Tonkin '' ( obecnie Hanoi ) w Wietnamie ( obydwie nazwy miast znaczą `` Wschodnia Stolica '' ) .
Również dawna stolica Japonii , Kioto , oraz Hansŏng ( ; obecnie Seul ) w Korei Południowej po przetłumaczeniu dadzą nazwę `` Stolica '' .
Nazwa Pekinu była wiele razy zmieniana .
Między 1928 a 1949 rokiem miasto nazywało się Beiping ( ) , co oznaczało `` Północny Pokój '' .
Yanjing ( ) to nieoficjalna nazwa miasta , używana podczas panowania dynastii Zhou .
Obecnie tę nazwę nosi miejscowe piwo .
Marco Polo w swoich relacjach nazwał Pekin z mongolska Cambaluc ( ) .
