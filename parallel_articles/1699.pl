Bejrut
Bejrut ( arab .
بيروت , `` Bayrūt '' , fr .
`` Beyrouth '' , gr .
`` Berytos '' ) – stolica i największe miasto Libanu , leżące w środkowej części kraju .
Liczy 2 mln 60 tys .
mieszkańców ( 2012 ) , większość pochodzenia arabskiego .
Dawniej miasto było nazywane : `` Birutu '' , `` Berytos '' .
Znajduje się tu wiele zabytków i dobytku historycznego kraju .
Bejrut stanowi siedzibę libańskiego rządu i odgrywa kluczową rolę w gospodarce kraju , bowiem w śródmieściu , a także bliskich centrum dzielnicach Hamra , Verdun i Aszrafijja znajdują się siedziby głównych przedsiębiorstw i banków .
Miasto jest również ważnym ośrodkiem życia kulturalnego .
Po zniszczeniach spowodowanych konfliktami zbrojnymi w Bejrucie prowadzone są ożywione prace renowacyjne zarówno w historycznym śródmieściu , jak i w dzielnicach turystycznych i handlowych .
