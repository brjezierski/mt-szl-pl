Bekerel , Bq – jednostka miary aktywności promieniotwórczej w układzie SI ( jednostka pochodna układu SI ) .
Próbka ma aktywność 1 Bq , gdy zachodzi w niej jeden rozpad promieniotwórczy na
sekundę .
Na przykład , jeśli jądra atomowe danego ciała emitują 1000 cząstek alfa na sekundę wskutek rozpadów alfa ( i jednemu rozpadowi odpowiada emisja jednej cząstki alfa oraz nie zachodzą inne rodzaje rozpadów ) , to aktywność tego ciała jest równa 1000 Bq , czyli 1 kBq .
Wymiarem bekerela jest s−1 .
Nazwa jednostki pochodzi od nazwiska Henriego Becquerela , który wraz z Pierre'em Curie i Marią Skłodowską-Curie otrzymał w 1903 roku Nagrodę Nobla za odkrycie i badanie promieniotwórczości .
Inną jednostką aktywności promieniotwórczej jest kiur ( Ci ) , odpowiadający aktywności 1 g radu-226 :
Dokładniejsze badania wykazały , że radioaktywność 1 g radu 226 wynosi 36,6 GBq .
Porównania .
Bekerel jest bardzo małą jednostką , dlatego w praktyce często używane są jej wielokrotności : kBq , MBq , GBq oraz TBq .
Na przykład aktywność naturalnego radionuklidu potasu 40K w ciele ludzkim wynosi średnio około 4 kBq , a więc 4000 rozpadów na sekundę .
Bomba atomowa użyta w Hiroszimie ( 14 kt ) wydzieliła substancje promieniotwórcze o aktywności około 8·1024 Bq .
