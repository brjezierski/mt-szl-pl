Belebej ( ros .
Белебей , `` Bielebiej '' ; baszk .
Бәләбәй , `` Bäläbäj '' ) – miasto w Republice Baszkirii , w Rosji , ośrodek administracyjny rejonu bielebiejewskiego .
Miejscowość założono w 1715 roku , a prawa miejskie nadano w 1781 .
W Belebeju urodzili się : Borys Nikołajewski , Władimir Bukowski , Inna Czurikowa .
