Belgrad
Belgrad ( , starożytne `` Singidunum '' , daw .
`` Białogród '' , ) – stolica i największe miasto Serbii położone na północy kraju przy ujściu Sawy do Dunaju ; jugosłowiańskie miasto-bohater .
Historia .
Belgrad należy do najstarszych miast Europy .
Jego długa i burzliwa historia sięga wstecz ponad 7000 lat .
Ziemie między Sawą i Dunajem były zamieszkiwane jeszcze w paleolicie .
Odkryte podczas prac archeologicznych w jaskini kamieniołomu Lesztanah w dzielnicy Czukarica liczne szczątki ludzkie ( takie jak kości i czaszki neandertalczyków ) pochodzą z początków paleolitu .
Pozostałości kultury z okresu późnego kamienia łupanego znaleziono w Vinczy , Czukaricy , na Żarkowie i w okolicach ujścia rzeki Sawy do Dunaju .
