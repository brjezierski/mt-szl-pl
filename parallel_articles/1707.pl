Belize City
Belize City – największe miasto Belize , historyczna stolica tego kraju , położone u ujścia rzeki Belize do Morza Karaibskiego .
Główny port morski kraju .
Ludność wynosi około 69 475 mieszkańców ( 2013 ) .
Belize City było stolicą Hondurasu Brytyjskiego do 1970 roku , kiedy brytyjski rząd kolonialny przeniósł się do nowej stolicy Belmopan .
Obecnie Belize City jest stolicą dyskryktu Belize .
Miasto zostało założone w XVII wieku przez Brytyjczyków jako Belize Town .
Znajduje się w nim Biblioteka Narodowa Belize .
W roku 1961 zostało zniszczone w wyniku przejścia huraganu .
