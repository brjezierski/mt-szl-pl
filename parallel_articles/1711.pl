Bellenberg
Bellenberg – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Szwabia , w regionie Donau-Iller , w powiecie Neu-Ulm .
Leży około 17 km na południe od Neu-Ulmu , przy autostradzie A7 i linii kolejowej Oberstdorf – Ulm .
Polityka .
Wójtem gminy jest Simone Vogt-Keller , jej poprzednikiem był Roland Bürzle , rada gminy składa się z 16 osób .
