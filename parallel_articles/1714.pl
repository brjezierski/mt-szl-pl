Bellinzona ( stacja kolejowa )
Bellinzona ( włoski : `` Stazione di Bellinzona '' ) – stacja kolejowa w Bellinzona , w kantonie Ticino , w Szwajcarii .
Historia .
Stacja została otwarta w 1874 jako `` Tessiner Talbahnen '' odcinkiem Biasca – Bellinzona – Lugano – Chiasso i Bellinzona – Locarno .
1882 otwarto Tunel kolejowy Świętego Gotarda i związaną z nim linię bezpośrednią Airolo - Biasca .
Po otwarciu TILO , w szybkiej kolei miejskiej Ticino i Lombardii , w 2004 Bellinzona stała się centrum sieci .
Od Bellinzony kursują pociągi podmiejskie do Biasca , Chiasso , Locarno lub Albate-Camerlata , w godzinach szczytu do Luino .
Wraz z otwarciem Gotthard Base Tunnel w 2017 r. , czas podróży między Bellinzona i niemieckojęzyczną Szwajcarią będzie skrócony o około jedną godzinę .
