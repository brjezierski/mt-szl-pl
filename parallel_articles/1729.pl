Beneluks
Beneluks , Benelux – region w Europie Zachodniej składający się z trzech sąsiadujących ze sobą monarchii : Belgii , Holandii i Luksemburga .
Słowo `` Benelux '' to skrót powstały z połączenia pierwszych sylab nazw tych państw ( francuska/niderlandzka , w nawiasach polska nazwa ) : Belgique/België ( Belgia ) , Nederland ( Holandia ) , Luxembourg/Luxemburg ( Luksemburg ) .
Pierwotnie określenie `` Benelux '' oznaczało jedynie unię celną wspomnianych państw .
Historia .
Unia gospodarcza i monetarna łącząca Belgię i Luksemburg ( Beluks ) została pierwotnie powołana w 1921 .
W 1943 przez rządy Belgii , Holandii i Luksemburga na uchodźstwie , przebywające w tym czasie w Londynie , została zawarta umowa monetarna .
Traktat ustanawiający unię celną został podpisany 5 września 1944 i wszedł w życie w 1948 .
Przestał obowiązywać w 1960 , kiedy to unia celna została zastąpiona Unią Ekonomiczną Beneluksu .
Powołanie tej organizacji przyczyniło się do powstania w późniejszym czasie Unii Europejskiej , której założycielami były właśnie kraje Beneluksu , a także RFN , Francja i Włochy .
W 1955 została utworzona Międzyparlamentarna Rada Konsultacyjna Beneluksu , nazwana potem nieoficjalnie parlamentem Beneluksu .
Zgromadzenie składa się z 21 członków parlamentu holenderskiego , 21 członków parlamentów narodowego i regionalnego Belgii i 7 członków parlamentu luksemburskiego .
