Bengaluru
Bengaluru ( , trb .
: `` Bengaluru '' , trl .
: `` Beṁgalūru '' ; ; ) – aglomeracja w południowych Indiach przekraczająca 10 milionów mieszkańców .
Znany ośrodek IT .
Geografia .
Położenie miasta na płaskowyżu Dekan ( 950 m n.p.m . )
chroni je przed zbyt wysokimi temperaturami , a leży ono w strefie tropikalnego klimatu sawann ( klasyfikacja Köppena – `` Aw '' ) .
Miasto znajduje się czterysta km na zachód od Ćennaju ( Madrasu ) .
Nazewnictwo .
1 listopada 2006 r. władze stanu Karnataka podjęły decyzję o zmianie nazwy miasta z angielskiej `` Bangalore '' na nazwę języku kannada – `` Bengaḷūru '' .
