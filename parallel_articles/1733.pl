Benguela ( prowincja )
Benguela ( port .
`` Província de Benguela '' ) – jedna z prowincji Angoli , znajdująca się w jej zachodniej części , na wybrzeżu Oceanu Atlantyckiego .
Od północy graniczy z prowincją Kwanza Południowa , od wschodu z Huambo i od południa z prowincjami Huíla i Namibe .
Według spisu z 2014 roku obejmuje ponad 2 miliony mieszkańców .
Stolicą prowincji jest Benguela .
Benguela zamieszkana jest głównie przez ludność Owimbundu i Ganguela .
Zajmują się oni uprawą roli , rybołówstwem i hodowlą zwierząt .
Benguela słynie ze swoich plaż Morena i Blue Bay , a także portu Lobito , który jest drugim najważniejszym portem w kraju .
Na obszarze prowincji żyją liczne gatunki zwierząt .
Wśród nich są zebry , lwy , antylopy , krokodyle , słonie , bawoły i lamparty .
