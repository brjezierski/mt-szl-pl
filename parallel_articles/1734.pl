Beni ( departament )
Beni – departament w północnej Boliwii .
Zajmuje powierzchnię 213 564 km² .
W 2012 roku liczył 422 008 mieszkańców , a gęstość zaludnienia wynosiła 1,98 mieszk./km² .
Stolicą departamentu jest Trinidad .
Administracyjnie departament dzieli się na 8 prowincji , w skład których wchodzi łącznie 48 kantonów .
