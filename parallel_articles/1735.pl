W 1943 roku został odsunięty od władzy , utworzył później satelicki względem Niemiec rząd rezydujący w północnej części kraju .
W 1945 roku został schwytany przez antyfaszystowskich włoskich partyzantów i rozstrzelany .
Wczesne lata .
Benito Mussolini urodził się 29 lipca 1883 w Dovia di Predappio , w gminie Forlì , w regionie administracyjnym Emilia-Romania jako syn Alessandra Mussoliniego i Rosy Maltoni .
Był najstarszym z trojga rodzeństwa .
Matka była nauczycielką , ojciec – kowalem i socjalistycznym aktywistą .
To on zdecydował o imionach nadanych Mussoliniemu – Benito na cześć Benita Juáreza ( Indianina , który został prezydentem Meksyku ) ; Amilcare i Andrea na cześć włoskich socjalistów Amilcarego Ciprianiego i Andrei Costy , natomiast młodszy brat Benita Mussoliniego otrzymał imię Arnaldo na cześć Arnolda z Brescii ( powstańca który doprowadził do wypędzenia papieża z Rzymu ) .
Początkowo uczył się pod kierunkiem matki .
W wieku 10 lat został oddany przez matkę do szkoły w Predappio , a rok później do gimnazjum oo .
salezjanów w Faenzy , przy niechętnej postawie ojca .
Wczesne poglądy polityczne Benita Mussoliniego zostały silnie ukształtowane pod wpływem poglądów ojca .
8 lipca 1901 ukończył Instytut Nauczycielski w Forlimpopoli , uzyskując uprawnienia nauczyciela szkół elementarnych .
Wkrótce również podjął pracę jako nauczyciel .
9 lipca 1902 roku wyjechał do Szwajcarii .
