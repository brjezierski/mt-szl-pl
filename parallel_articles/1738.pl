Benoît Sokal
Benoît Sokal ( ur .
28 czerwca 1954 w Brukseli , zm .
28 maja 2021 ) – belgijski twórca komiksów i grafiki do gier komputerowych .
Związany z tworzącą gry komputerowe firmą Microïds , dla której projektował grafikę do takich gier jak `` Amerzone '' i gry z serii `` Syberia '' .
Życiorys .
Benoît Sokal studiował na École Supérieure des Arts Saint-Luc w Brukseli wraz z wieloma belgijskimi twórcami komiksów , np .
François Schuitenem .
W 1978 zaczął rysować dla magazynu „ À Suivre ” .
Stworzył serię komiksów `` Inspektor Canardo '' opowiadających o losach załamanego kaczora-detektywa z zamiłowaniem do papierosów , alkoholu i femme fatale .
Później Sokal dołączył do tworzącej gry komputerowe firmy Microïds i zaczął pracować nad grafiką do gier przygodowych , takich jak m.in .
`` Amerzone '' ( 1999 ) i `` Syberia '' ( 2002 ) .
Po kilku latach założył własną firmę White Birds Productions , która wyprodukowała grę `` Paradise '' wydaną przez Ubisoft .
