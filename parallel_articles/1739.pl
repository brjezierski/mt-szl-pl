Bento Gonçalves
Bento Gonçalves – miasto w południowej Brazylii , w stanie Rio Grande do Sul .
Drugi pod względem produkcji win region Brazylii i pierwszy pod względem jakości .
Założone w 1870 roku i zasiedlone przez włoskich emigrantów .
Historia .
W 1875 roku pierwsi włoscy emigranci przybywają na tereny dzisiejszego Bento Gonçalves i osiadają na terenach Colônia Dona Isabel .
