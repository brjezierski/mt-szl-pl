Beppu
– miasto portowe położone w Japonii , w prefekturze Ōita , w północno-wschodniej części wyspy Kiusiu , nad zatoką Beppu .
W mieście swoje siedziby mają instytuty badawczo-naukowe ( rolniczy i balneologiczny ) , zarządzające m.in .
wielkim akwarium morskim i muzeum wulkanologicznym .
W pobliżu znajduje się Park Narodowy Aso-Kujū .
Gorące źródła .
Beppu jest miejscowością turystyczną położoną między morzem a górami .
Jest znanym uzdrowiskiem z gorącymi źródłami ( onsen ) mineralnymi oraz kąpieliskami morskimi .
W mieście znajduje się dziewięć głównych geotermalnych plam gorąca , które są nazywane „ dziewięcioma piekłami Beppu ” .
