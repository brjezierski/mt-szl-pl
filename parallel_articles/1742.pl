Języki berberyjskie
Języki berberyjskie ( `` berberskie '' , `` amazygijskie '' ) – podrodzina języków afroazjatyckich , używanych głównie na terenach gór Atlas w Maroku ( gałąź północna ) oraz w Libii , Algierii i Nigrze ( gałąź południowa ) .
Widoczna wydaje się unifikacja językowa północnych plemion berberyjskich wyrażająca się w przyjęciu wspólnej wersji języka zwanej tamazight , plemiona południowe ( Tuaregowie ) posługują się natomiast głównie językiem tamaszek .
Piśmiennictwo istnieje od starożytności , a do zapisu tradycyjnie używa się alfabetu tifinagh .
Językami berberyjskimi posługuje się ok. 22 mln ludzi .
