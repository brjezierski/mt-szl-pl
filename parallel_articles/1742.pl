Liczba użytkowników .
Szacunki dotyczące liczby użytkowników poszczególnych języków berberyjskich są bardzo rozbieżne , zaś różnice pomiędzy skrajnymi wariantami liczebności są kilkukrotne .
Określenie dokładnej liczby mówiących poszczególnymi językami jest utrudnione także z tego powodu , że duża część Berberów to osoby dwujęzyczne , posługujące się zarówno którymś z języków berberyjskich ( niekiedy dwoma ) , jak i językiem arabskim .
