Berehowo leży na Nizinie Zakarpackiej nad rzeką Werką , około 70 km na południowy wschód od Użhorodu , 5 km od drogowego przejścia granicznego Łużanka – Beregsurany .
Słynie z tradycji uprawy winogron i produkcji win .
W okolicach miasta występują wody termalne i mineralne , leczące przede wszystkim dolegliwości układu krążenia , oddechowego , sercowo-naczyniowego czy chorób skóry .
Miasto `` Lamperthaza '' zostało założone przez księcia Lamberta , syna króla Węgier Beli I. Podczas najazdu mongolskiego w latach 1240–1241 miasto zostało zniszczone .
W celu jego odbudowy i ponownego zaludnienia okolicy król Bela IV Wielki sprowadził tu niemieckich osadników z Saksonii , którym następnie udzielił pewnych przywilejów handlowych .
Od 1271 było siedzibą władz komitatu Bereg .
W 1342 Bereg Saski stał się wolnym miastem królewskim z kolejnymi przywilejami .
W 1504 po raz pierwszy pojawia się nazwa `` Beregszasz '' .
W 1566 zostało zniszczone przez Tatarów , 17 czerwca 1657 zostało spalone przez wojska polskie , w 1686 – przez wojska habsburskie .
22 maja 1703 na miejskim rynku Tomasz Ese ogłosił powstanie przeciwko Habsburgom .
Po stłumieniu powstania cesarz Karol VI Habsburg skonfiskował miasto , wraz z pozostałymi dobrami , rodowi Rakoczych i w 1728 nadał je rodowi Schönborn .
