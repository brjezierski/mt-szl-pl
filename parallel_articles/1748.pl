Bereźne
Bereźne ( ukr .
Березне , `` Berezne '' ) – miasto na Ukrainie , w obwodzie rówieńskim , siedziba rejonu berezieńskiego .
Bereźne leży na Wołyniu nad Słuczą .
Historia .
W 1446 roku miasteczko Bereźne ( dawne nazwy : Bereźno , Berezno , Jędrzejów ) było własnością Dymitra , któremu przywrócił je wraz z innymi dobrami wielki książę litewski Świdrygiełło .
Następnie należało do dóbr Paszka Dochnowieza , którego córka wniosła je jako wiano kniaziowi Michałowi Hołowni .
W połowie XVII wieku miejscowość była własnością Jana Daniłowicza .
Następnie należała do Korzeniowskich i Michała Małyńskiego .
