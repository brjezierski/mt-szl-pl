Wzgórze ( anatomia )
Wzgórze ( łac .
`` thalamus '' , ang .
thalamus ) – część międzymózgowia znajdująca się pod spoidłem wielkim .
Przylega do niego podwzgórze .
Odpowiada za wstępną ocenę bodźców zmysłowych i przesyłanie ich do kory mózgowej ( oprócz węchu ) .
Pełni kluczową rolę w integracji informacji czuciowych i ruchowych , w procesach uwagi i kontrolowania dostępu do danych czuciowych .
Stanowi największą ilość istoty szarej międzymózgowia .
Wzgórze tworzą skupienia istoty szarej ( jądra ) pooddzielane przez pasma istoty białej .
Stanowi ograniczenie komór : komory bocznej oraz komory trzeciej .
Budowa zewnętrzna .
W opisie anatomicznym wzgórza wyróżnia się dwa bieguny i cztery powierzchnie .
Budowa wewnętrzna , obszary wzgórza i funkcje .
Wzgórze jest zbudowane głównie z istoty szarej , tworzącej jądra , do której wnikają pasma istoty białej , zwanymi blaszkami rdzennymi .
Wzdłuż bocznej powierzchni wzgórza przebiega blaszka rdzenna zewnętrzna , która oddziela leżące bocznie od niej jądro siatkowate wzgórza od pozostałych jąder .
Blaszka rdzenna wewnętrzna przebiega strzałkowo , oddzielając jądro przyśrodkowe od jąder bocznych .
Ku przodowi rozdziela się , ograniczając jądra przednie .
Z tyłu blaszka zagina się , oddzielając jądro przyśrodkowe od jąder poduszki .
Wewnątrz blaszki znajduje się jądro śródblaszkowe .
