Bergamo ( stacja kolejowa )
Bergamo ( wł : `` Stazione di Bergamo '' ) – stacja kolejowa w Bergamo , w regionie Lombardia , we Włoszech .
Znajdują się tu 3 perony .
Historia .
Stacja została wybudowana w ramach projektu kolei Südbahn , część linii Ferdinandea ( Mediolan - Wenecja ) i została otwarta 12 października 1857 r. , wraz z odcinkiem Treviglio - Bergamo - Coccaglio .
W 1863 roku została uruchomiona linia do Lecco .
