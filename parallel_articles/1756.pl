Bergerac ( serial telewizyjny )
Bergerac – brytyjski serial telewizyjny , wyprodukowany przez BBC i emitowany w latach 1981-1991 .
Powstało 87 jednogodzinnych odcinków .
Twórcą serii był Robert Banks Stewart .
Czołówkę muzyczną stworzył George Fenton .
Fabuła .
Akcja serialu rozgrywa się na malowniczej ( będącej dependencją korony brytyjskiej ) wyspie Jersey , w Anglii i we Francji .
W tytułowej roli rozwiązującego różne zagadki detektywa fikcyjnego `` Bureau des Etrangers '' , sierżanta Jima Bergeraca , wystąpił John Nettles .
Bergerac ma charakterystyczny samochód : burgundowy Triumph Roadster z roku 1947 .
Pozostała obsada .
W epizodach wystąpili m.in .
: Barry Foster ( 1985 r. ) , Michael Gambon ( 1987 ) , Julian Glover ( 1990 ) , Greta Scacchi ( 1981 ) , Prunella Scales ( 1981 ) , Richard Griffiths ( 1983 ) , Joanne Whalley ( 1983 ) , Connie Booth ( 1987 ) , Art Malik ( 1983 ) , Bill Nighy ( 1991 ) , Natascha McElhone ( 1991 ) i Ray Winstone ( 1983 ) .
