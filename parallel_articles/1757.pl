Mundur górniczy
Mundur górniczy ( śl .
: `` bergmůńok '' ) – uniform przeznaczony dla osób posiadających stopień górniczy .
Mundur górniczy jest symbolem przynależności do górniczego stanu , wyrazem tradycji i szczególnego uznania dla ciężkiej i odpowiedzialnej pracy górniczej .
Mundur górniczy w Polsce .
Stosowne przepisy zawarte są w ustawie o stopniach górniczych , honorowych szpadach górniczych i mundurach górniczych .
Mundur .
Wzorem dla dzisiejszego munduru górniczego był uniform wprowadzony w XVIII wieku na terenie Niemiec w Zagłębiu Ruhry .
W Polsce pierwsze mundury wprowadzone zostały w 1817 roku i służyły jako odzież robocza .
