Berszad
Berszad ( ukr .
`` Бе́ршадь '' ) – miasto na Ukrainie , w obwodzie winnickim , siedziba władz rejonu berszadzkiego .
Historycznie miasto leży na wschodnim Podolu .
Prywatne miasto szlacheckie położone w województwie bracławskim było w 1789 własnością Fryderyka Józefa Moszyńskiego .
Historia .
Pierwsza wzmianka o miejscowości pochodzi z 1459 .
W miejscowości istniał zamek .
W 1617 roku został zburzony na żądanie Turcji , na co zgodzono się w Traktacie w Buszy .
