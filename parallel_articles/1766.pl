Bertel Thorvaldsen
Bertel Thorvaldsen ( ur .
19 listopada 1770 w Kopenhadze , zm .
24 marca 1844 tamże ) – duński rzeźbiarz islandzkiego pochodzenia , jeden z czołowych przedstawicieli klasycyzmu , twórca m.in .
pomnika Mikołaja Kopernika i pomnika księcia Józefa Poniatowskiego w Warszawie .
Rodzina i młodość .
Urodził się w Kopenhadze w 1770 roku ( niektóre dane podają 1768 ) .
Jego ojcem był Islandczyk Gottskálk Þorvaldsson , który za chlebem przywędrował do stolicy Danii , gdzie rzeźbił galiony na dzioby ówczesnych drewnianych statków .
Jego matka natomiast była Dunką , pochodzącą z Jutlandii , gdzie posiadała gospodarstwo .
Tradycyjnie według islandzkich obyczajów powinien się on nazywać Gottskaldsson , jednak w Kopenhadze nie było tego zwyczaju i nazwano go po duńsku .
Dzieciństwo i młodość Thorvaldsen spędził właśnie w Danii , chociaż często uważa się go za rzeźbiarza islandzkiego .
Nauka i wykształcenie .
Kiedy Bertel skończył jedenaście lat , matka która zauważyła u niego talent do rysowania , zapisała go w tajemnicy przed mężem do Akademii Sztuk Pięknych w Kopenhadze .
Tam młody artysta wykazał się wielkim talentem .
Jego prace zdobywały liczne nagrody , a on sam medale np .
`` Odpoczywającego Amora '' lub `` Priama z Achillesem '' .
Jedyną osobą , której sukcesy Bertela nie cieszyły był jego ojciec , który chciał , aby syn w przyszłości objął po nim schedę w warsztacie .
Bertel wykorzystał pracę u ojca , jako swoistego rodzaju praktyki .
W 1797 roku zdobył złoty medal za relief pod tytułem `` Piotr uzdrawiający paralityka '' , dzięki czemu został zauważony na uczelni .
Wówczas Christian Ditlev Frederick , hrabia Reventlow załatwił młodemu rzeźbiarzowi trzyletnie stypendium w Rzymie .
Było ono jednak niewystarczające i Bertel musiał się mocno postarać , aby sam wyjazd do Włoch mógł się odbyć .
