Siedzibą gminy były Berżniki .
Za Królestwa Polskiego gmina należała do powiatu sejneńskiego w guberni suwalskiej .
Na początku okresu międzywojennego gmina Berżniki należała do powiatu sejneńskiego w woj .
białostockim .
Wraz ze zniesieniem powiatu sejneńskiego z dniem 1 stycznia 1925 roku gminę przyłączono do powiatu suwalskiego w tymże województwie .
Po wojnie gmina zachowała przynależność administracyjną .
W dniu 1 lipca 1952 roku gmina składała się z 26 gromad : Berżniki , Berżniki kol. , Berżałowce , Bosse , Degucie , Dubowo , Dworczysko , Grudziewszczyzna , Hołny Wolmera , Krejwińce , Kukle , Markiszki , Ogrodniki , Olszanka , Poćkuny , Podlaski , Posejnele , Posejny , Półkoty , Rachelany , Sztabinki , Świeckie , Wigrańce , Zaleskie , Zaruby , Żegary .
Gmina została zniesiona 29 września 1954 roku wraz z reformą wprowadzającą gromady w miejsce gmin .
Gminę Berżniki reaktywowano 1 stycznia 1973 roku w woj .
