Beuthener SuSV
Beuthener Spiel- und Sport-Verein 1909 e.V .
( pierwotnie `` Britannia Beuthen '' , znany również jako `` Beuthener SuSV 09 '' , `` SuSV 09 '' , `` Beuthen 09 '' ) – niemiecki klub piłkarski , który działał w latach 1909–1945 w Bytomiu .
Historia .
Klub został założony 15 czerwca 1909 roku .
W 1911 roku został przemianowany na `` SuSV Beuthen '' .
W sezonie 1914 klub został mistrzem Górnego Śląska .
W czasie I wojny światowej rozgrywki zostały zawieszone .
Po wojnie w dwóch następnych sezonach pod rząd : 1920 i 1921 SuSV 09 Beuthen został ponownie mistrzem Górnego Śląska .
W 1920 w okresie plebiscytowym przegrał 2-3 mecz z Pogonią Lwów ( wzmocnioną zawodnikami innych drużyn lwowskich i warszawskich ) rozgrywającą mecze o znaczeniu propagandowym na Górnym Śląsku .
Klub ten zapłacił za możliwość rozegrania meczu z Polakami .
W 1922 roku część Górnego Śląska została przyłączona do Rzeczypospolitej Polskiej , Bytom pozostał w Niemczech .
W sezonie 1922/1923 klub zdobył mistrzostwo niemieckiej części Górnego Śląska i zakwalifikował się do wyższych rozgrywek , zdobywając wicemistrzostwo Południowo-Wschodnich Niemiec .
W rozgrywkach niemieckich najpierw walczono o mistrzostwo swojego okręgu - w tym wypadku Górnego Śląska lub Śląska , później o mistrzostwo swojej części Niemiec - w tym wypadku Południowo-Wschodnich Niemiec ( niem : `` Südost-Deutschland '' ) .
Mistrz Południowo-Wschodnich Niemiec grał już systemem pucharowym play-off o mistrzostwo całego kraju .
