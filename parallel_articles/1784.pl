Beyoncé ( album )
Beyoncé – piąty album studyjny amerykańskiej wokalistki Beyoncé Knowles , wydany 13 grudnia 2013 roku nakładem Columbia Records i Parkwood Entertainment .
Został „ albumem wizualnym ” , ponieważ każdemu z nagrań na płycie towarzyszy teledysk .
`` Widzę muzykę '' – mówi Beyoncé wyjaśniając swoją decyzję o wydaniu płyty „ wizualnej ” .
`` Kiedy się w nią wsłuchuję , od razu ją sobie wizualizuję .
Dla mnie emocje to seria obrazów – wspomnień z dzieciństwa , przemyśleń , marzeń czy zapamiętanych snów .
I wszystkie nieodłącznie związane są z muzyką '' .
Na płycie znajduje się 14 premierowych piosenek utrzymanych w klimacie pop , electro-R & amp ; B i soul oraz 17 wideoklipów nakręconych w różnych miejscach na świecie – od rodzinnego Houston , przez Nowy Jork , Paryż , aż do Sydney i Rio de Janeiro .
Poruszone na nim tematy dotyczą feministycznego podejścia do płci , monogamicznej miłości oraz kłopotów w związku .
Były one inspirowane pełną swobodą twórczą Beyoncé .
