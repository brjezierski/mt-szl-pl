Bezrzecze ( Szczecin )
Bezrzecze ( niem .
`` Brunn '' ) – część miasta Szczecina na osiedlu Krzekowo-Bezrzecze , położone przy granicy miasta z gminą Dobra sąsiadujące z wsią o tej samej nazwie zwaną potocznie Bezrzeczem Górnym .
Bezrzecze zostało włączone w granice Szczecina w 1939 podczas tworzenia tzw .
Wielkiego Miasta Szczecina .
Do 1945 roku wieś nosiła nazwę `` Brunn '' natomiast nazwa `` Zdrój '' , będąca dosłownym tłumaczeniem nazwy niemieckiej , wraz z nazwą `` Broń '' były nazwami przejściowymi od 1945 r. W obecnym brzmieniu nazwa została administracyjnie zatwierdzona 12 listopada 1946 .
W 1954 część wschodnia Bezrzecza , zwana potocznie Bezrzeczem Dolnym , została ponownie przyłączona do miasta .
Bezrzecze ma typowo mieszkaniowy charakter , brak jest zakładów przemysłowych i usługowych .
Zabudowa pochodzi z okresu międzywojennego i powojennego .
Główną ulicą dojazdową jest Koralowa położona w jego południowej części .
Komunikację z innymi dzielnicami zapewniają linie autobusowe 74 i 105 ( dzienne ) oraz 527 ( nocna ) .
Bezrzecze zostało upamiętnione w piosence `` Do Ciebie , Aniu , szłem '' szczecińskiego rapera Łony .
