Bełchatów
Bełchatów – miasto w województwie łódzkim , siedziba powiatu bełchatowskiego .
Położone na Wzniesieniach Południowomazowieckich , w mezoregionie Wysoczyzna Bełchatowska nad rzeką Rakówką , ok. 50 km na południe od Łodzi i ok. 25 km na zachód od Piotrkowa Trybunalskiego .
Bełchatów położony jest w historycznej ziemi sieradzkiej .
Uzyskał lokację miejską w 1737 roku , zdegradowany w 1870 roku , ponowne nadanie praw miejskich w 1925 roku .
Miasto prywatne Królestwa Kongresowego , położone było w 1827 roku w powiecie piotrkowskim , obwodzie piotrkowskim województwa kaliskiego .
Bełchatów jest jednym z 312 miast należących do Związku Miast Polskich .
W latach 1975–1998 miasto administracyjnie należało do województwa piotrkowskiego .
Według danych GUS z 31 grudnia 2019 roku miasto liczyło 56 973 mieszkańców .
