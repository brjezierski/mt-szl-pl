Bełdno
Bełdno – wieś w Polsce położona w województwie małopolskim , w powiecie bocheńskim , w gminie Żegocina .
Wieś Bedlna położona w końcu XVI wieku w powiecie szczyrzyckim województwa krakowskiego była własnością klasztoru kanoników regularnych w Trzcianie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnowskiego .
Integralne części miejscowości : Granice , Mrowcówka , Podlas , Połom , Siandajówka .
