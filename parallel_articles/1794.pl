Bełżyce
Bełżyce – miasto w Polsce , w województwie lubelskim , w powiecie lubelskim , siedziba gminy miejsko-wiejskiej Bełżyce .
Położone na Równinie Bełżyckiej .
Prywatne miasto szlacheckie lokowane w 1417 roku , położone było w drugiej połowie XVI wieku w powiecie lubelskim w województwie lubelskim .
Miasto prywatne Królestwa Kongresowego , położone było w 1827 roku w powiecie lubelskim , obwodzie lubelskim województwa lubelskiego .
Miasto zdegradowane w 1869 roku , ponowne nadanie praw miejskich w 1958 roku .
W latach 1975–1998 miasto administracyjnie należało do starego woj .
