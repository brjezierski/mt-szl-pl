Bhutan
Bhutan ( dzongkha འབྲུག་ཡུལ་ , ang .
`` Bhutan '' ) , oficjalnie Królestwo Bhutanu ( dzongkha འབྲུག་ཡུལ་ , ang .
`` Kingdom of Bhutan '' ) – państwo śródlądowe w Azji Południowej , we wschodnich Himalajach , graniczące na północy , zachodzie i wschodzie z Chinami , a na południu z Indiami .
Oficjalna nazwa `` Druk Jul '' oznacza Królestwo Smoka .
Mieszkańcy Bhutanu nazywają siebie `` Druk Pa '' – ludzie smoków .
Geografia i klimat .
Bhutan jest górzystym krajem bez dostępu do morza , leżącym we wschodniej części Himalajów , pomiędzy Tybetem w Chinach na północy , a indyjskimi stanami Asam i Bengal Zachodni na południu i wschodzie oraz Sikkim na zachodzie .
Bhutan dzieli się na trzy równoleżnikowe strefy : na północy wznoszą się Himalaje Wysokie .
