Jest to równina Duars ( Dwar ) leżąca w znacznej większości na terenie Indii .
Tu zajmuje pas przygraniczny z Indiami o szerokości 8–13 km .
Ponad połowa terytorium kraju leży powyżej 3000 m n.p.m .
Sieć rzeczna jest gęsta , wszystkie są dopływami Brahmaputry , do której wpadają już na terenie Indii .
Klimat zwrotnikowy wilgotny , monsunowy ; tropikalny na południu , chłodne zimy i gorące lata w dolinach , ostre zimy i chłodne lata w Himalajach .
Granica wiecznych śniegów wskutek znacznych opadów przebiega stosunkowo nisko , bo już na wysokości ok. 4500–4880 m n.p.m .
Góry do 3500 m są pokryte lasami zajmującymi 67 % powierzchni kraju .
