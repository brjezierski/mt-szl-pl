Według autora , celem adaptacji było m.in .
ukazanie podobieństwa wydarzeń biblijnych do wydarzeń w dziejach Śląska .
Inny cel to zachowanie odrębności kulturowej Górnego Śląska i stworzenie wyższej literatury w języku jego mieszkańców .
Rada Języka Polskiego poddała Biblię Ślązoka – podobnie jak inne adaptacje na regionalne odmiany języka – zdecydowanej krytyce .
Jak podkreślili w opracowaniu eksperci z Komisji Języka Religijnego RJP – razi niezgodność z treścią Biblii , nieuzasadniona poufałość i teologiczna beztroska , np .
Bóg Ojciec i Jezus to w Biblii Ślązoka – „ Ponboczek ” , a lekko lekceważące słowo „ frelka ” , czyli „ dziewczyna ” , jest zastosowane w odniesieniu do Marii z Nazaretu .
