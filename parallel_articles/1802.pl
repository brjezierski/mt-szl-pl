Biblia
Biblia , Pismo Święte ( z greckiego , `` biblion '' „ zwój papirusu , księga ” , l.m .
, `` biblia '' „ księgi ” ) – zbiór ksiąg , spisanych pierwotnie w językach hebrajskim , aramejskim i w greckim ( w formie `` koinè '' ( gr .
κοινὴ ) ) , uznawanych przez żydów i chrześcijan za natchnione przez Boga .
Biblia i poszczególne jej części posiadają odmienne znaczenie religijne dla różnych wyznań .
Na chrześcijańską Biblię składają się Stary Testament i Nowy Testament .
Biblia hebrajska – Tanach – obejmuje księgi Starego Testamentu .
Poszczególne odłamy i tradycje chrześcijańskie mają nieco inny kanon ksiąg świętych .
