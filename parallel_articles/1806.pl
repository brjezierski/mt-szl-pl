Bielefeld
Bielefeld – miasto na prawach powiatu w Niemczech , w północno-wschodniej części kraju związkowego Nadrenia Północna-Westfalia , w rejencji Detmold .
To największe miasto w rejonie Ostwestfalen-Lippe , centrum kulturalne i gospodarcze .
