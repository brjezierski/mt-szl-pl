Podział administracyjny .
Bielefeld podzielony jest na dziesięć dzielnic .
Każda z nich zarządzana jest przez złożoną z 19 wybieranych przez mieszkańców członków radę dzielnicową ( `` Bezirksvertretung '' ) .
Historia .
Bielefeld otrzymał prawa miejskie w 1214 roku .
Miasto pierwotnie należało do hrabstwa Ravensbergu , od 1346 roku połączonego z księstwami Jülich i Bergu , wraz z którym w 1647 roku znalazło się pod panowaniem Brandenburgii .
Od XVI wieku zaczął się w Bielefeldzie rozwijać przemysł tekstylny .
W 1930 roku liczba mieszkańców przekroczyła 100 000 .
W czasie II wojny światowej miasto zostało znacznie zniszczone wskutek bombardowań .
Transport .
Z północy na południe miasta przebiega droga ekspresowa Ostwestfalendamm , która leży w ciągu drogi krajowej B61 .
Głównym operatorem komunikacji miejskiej jest spółka `` moBiel GmbH '' , córka komunalnego przedsiębiorstwa `` Stadtwerke Bielefeld GmbH '' .
Komunikacja opiera się na zbliżonej do siedmioramiennej gwiazdy sieci szynowej oraz uzupełniającej ją sieci autobusowej .
Komunikacja szynowa o prześwicie toru 1000 mm i napięciu zasilania 750 V nosi cechy tramwaju , metra i kolei podmiejskiej .
W centrum miasta pojazdy poruszają się w tunelach , w większym oddaleniu od centrum podobnie do klasycznego tramwaju – tory są ułożone w ulicach , natomiast na przedmieściach torowiska są wydzielone a przejazdy wyposażone w sygnalizację zbliżoną do kolejowej .
Istnieją cztery stałe podstawowe linie tramwajowe i trzy zjazdowe .
