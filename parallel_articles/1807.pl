Biella
Biella – miasto i gmina we Włoszech , w regionie Piemont , w prowincji Biella .
Według danych na styczeń 2009 gminę zamieszkiwało 45 845 osób przy gęstości zaludnienia 982,1 os./1 km² .
W 2007 odbył się tu turniej tenisowy ITF , który wygrała Agnieszka Radwańska .
Na terenie gminy znajduje się sanktuarium w Oropie i katedra w Bielli .
