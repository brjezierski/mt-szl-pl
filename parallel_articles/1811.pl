Bilbao
Bilbao ( bask .
`` Bilbo '' ) – miasto w północnej Hiszpanii , w regionie Kraj Basków , u ujścia rzeki Nervión do Zatoki Biskajskiej Oceanu Atlantyckiego , siedziba administracyjna prowincji Bizkaia .
W 2019 liczyło ok. 346 405 mieszkańców .
Wraz z przyległymi miejscowościami tworzy obszar metropolitalny `` Gran Bilbao '' .
