Bilecik
Bilecik – miasto i prowincja w Turcji .
Według danych na rok 2000 miasto zamieszkiwało 34 105 osób , a według danych na rok 2004 całą prowincję ok. 199 000 osób .
Gęstość zaludnienia w prowincji wynosiła ok. 46 osób na km² .
Miasto leży nad rzeką Karasu , dopływem Sakaryi .
Stanowi ośrodek handlowy dla regionu rolniczego , ponadto drobny przemysł .
Z uwagi na dobre połączenia zarówno drogowe jak i kolejowe z większymi ośrodkami : Adapazarı ( ok. 100 km na północ ) i Eskişehir ( ok. 75 km .
na południowy wschód ) , miasto zaczęło przyciągać turystów .
Bilecik posiada bogatą historię , stałe osadnictwo datowane jest na 2000 lat p.n.e .
W 1298 zostało zdobyte przez Osmana Pierwszego i z rąk Bizancjum przeszło pod panowanie Imperium Osmańskiego .
Część miasta została zniszczona podczas wojny grecko-tureckiej 1919-1920 .
Bilecik posiada zabytkowe stare miasto , położone na wzgórzach w centrum miejscowości .
W pobliżu mauzolea Seyha Edebali , ojczyma założyciela Imperium Osmańskiego , Osmana I , oraz Orchana , syna pierwszego władcy .
