Bilzen
Bilzen – miasto i gmina w Belgii , w prowincji Limburgia , niedaleko Hasselt .
W dniu 1 stycznia 2006 Bilzen liczyło 30 057 mieszkańców .
Powierzchnia miasta to 75,9 km² .
Gmina Bilzen składa się z miasta Bilzen oraz wiosek Beverst , Eigenbilzen , Grote-Spouwen , Hees , Hoelbeek , Kleine-Spouwen , Martenslinde , Mopertingen , Munsterbilzen , Rijkhoven , Rosmeer , Schoonbeek i Waltwilder .
W 1977 r. scaliły się one w jedną gminę .
