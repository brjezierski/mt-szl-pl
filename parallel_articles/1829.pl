Birgu
Birgu ( ) , również znane jako Vittoriosa , w przeszłości Citta Vittoriosa ( Miasto Zwycięskie ) – stare ufortyfikowane miasto po południowej stronie Grand Harbour , jedna z jednostek administracyjnych na Malcie .
Miasto zajmuje cypel z fortem Saint Angelo na czele i miastem Cospicua u jego podstawy .
Birgu jest idealnym miejscem do bezpiecznego kotwiczenia , a z czasem rozwinęło bardzo długą historię działalności morskiej , handlowej i wojskowej .
Birgu to bardzo stara miejscowość , której początki sięgają czasów średniowiecza .
Przed ustanowieniem Valletty stolicą i głównym miastem Malty , mocarstwa , które chciały rządzić wyspami maltańskimi musiały przejąć kontrolę nad Birgu ze względu na jego znaczącą pozycję w Grand Harbour .
Miasto w rzeczywistości służyło jako baza rycerzy Zakonu Szpitalników św .
Jana i „ de facto ” stolica Malty od 1530 do 1571 .
Birgu jest dobrze znane ze swojej kluczowej roli w Wielkim Oblężeniu Malty w 1565 .
