Birkenhead
Birkenhead – miasto portowe w Wielkiej Brytanii ( Anglia ) , u ujścia rzeki Mersey do Morza Irlandzkiego .
Miasto jest połączone dwoma tunelami wybudowanymi w 1934 roku z leżącym naprzeciw Liverpoolem .
W Birkenhead rozwinął się przemysł stoczniowy , spożywczy , maszynowy , elektrotechniczny , metalowy oraz odzieżowy .
Pierwsi mieszkańcy osiedlali się w XII wieku wokół opactwa benedyktynów .
W roku 1829 w porcie zwodowano pierwszy , całkowicie metalowy brytyjski statek .
