Birkirkara
Birkirkara – jedna z jednostek administracyjnych na Malcie , zamieszkana przez 22 247 osób .
Znajduje się tu browar Simonds Farsons Cisk oraz centrum biznesowe The Quad Business Centre .
Sport .
W miejscowości funkcjonuje klub piłkarski Birkirkara FC .
Powstał w 1950 roku .
Obecnie gra w najwyższej maltańskiej lidze – Maltese Premier League .
