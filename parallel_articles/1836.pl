Birstall ( Leicestershire )
Birstall – wieś w Anglii , w hrabstwie Leicestershire , w dystrykcie Charnwood .
Leży 5 km na północ od miasta Leicester i 147 km na północny zachód od Londynu .
W 2004 miejscowość liczyła 11 480 mieszkańców .
History .
Nazwa Birstal pochodzi od staroangielskiego słowa Burhsteall oznaczającego `` stary porzucony fort '' .
