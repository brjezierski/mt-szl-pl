Birsztany
Birsztany ( lit .
`` Birštonas '' ) – miasto na Litwie , nad Niemnem , na południe od Kowna .
Znane od XIV w. , od 1966 miasto .
Ok. 3 tys .
mieszkańców ( 2005 ) .
Położone na prawym brzegu Niemna , w miejscu gdzie rzeka tworzy malownicze meandry .
Miasto otaczają sosnowe lasy .
Są miejscem kuracji i wypoczynku .
Są tu sanatoria .
Wody mineralne zawierają m.in .
potas i magnez .
Historia .
W średniowieczu była tu warownia i miejsce polowań Wielkich Książąt Litewskich .
W XIV w. otrzymały prawa miejskie .
Kurortem zostały w XIX w. , kiedy wybudowano ujęcie wody mineralnej i odkryto pokłady borowiny .
Po I wojnie światowej znalazły się w granicach Litwy i awansowały do rangi kurortu litewskiego , ale po II wojnie światowej wysunęły się przed nie Druskieniki .
