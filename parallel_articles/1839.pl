Wólka Biska
Wólka Biska – wieś w Polsce położona w województwie lubelskim , w powiecie biłgorajskim , w gminie Biszcza .
Leży na Płaskowyżu Tarnogrodzkim , u ujścia Łazownej do Tanwi .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 133 mieszkańców i była czwartą co do wielkości miejscowością gminy Biszcza .
Historia .
Biszcza i Biska Wólka , opisane w wieku XIX wspólnie jako wsie w powiecie biłgorajskim , gminie Biszcza , parafii Tarnogród .
Jest to jak opisuje Słownik duża wieś , ciągnąca się na milę przeszło w bezleśnej okolicy , na wyniosłości 646 m.n.p nad morza .
Posiada urząd gminny i cerkiew dla ludności rusińskiej .
Należy do ordynacji Zamojskich .
W 1827 r. było tu 238 domów i 2222 mieszkańców .
