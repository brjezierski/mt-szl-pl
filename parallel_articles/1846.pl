Bisporella sulfurina ( Quél . )
S.E .
Carp .
– gatunek grzybów z rodziny `` Helotiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1880 L. Quélet nadając mu nazwę `` Helotium sulfurinum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1974 Steven E. Carpenter .
Synonimy :
Morfologia .
Typu apotecjum .
Początkowo kuliste i zamknięte , potem podczas wzrostu i dojrzewania przybierają kształt poduszkowaty lub dyskowaty z wklęsłością na środku .
Średnica 0,5–1 mm .
Powierzchnia hymenialna naga , jasnożółta lub cytrynowa , lekko przeźroczysta .
