Bissau
Bissau – stolica Gwinei Bissau .
Jest największym miastem i głównym portem kraju z 427 400 mieszkańcami ( 2007 ) .
Położone na wyspie u ujścia rzeki Gêba do Oceanu Atlantyckiego .
Historia .
Miasto założone zostało w 1687 przez Portugalczyków jako ufortyfikowany port , będąc w tamtych czasach ośrodkiem handlu niewolnikami .
W XIX wieku nabrało rangi największego centrum handlowego na terenie Gwinei Portugalskiej .
W 1942 roku miasto stało się stolicą regionu .
W 1974 roku zostało stolicą niepodległej Gwinei Bissau .
