Bitwa pod Szawlami
Bitwa pod Szawlami ( niem .
: `` Schlacht von Schaulen '' , lit .
: `` Saulės mūšis '' lub `` Šiaulių mūšis '' , łot .
: `` Saules kauja '' lub `` Kauja pie Saulės '' ) – starcie zbrojne , które miało miejsce 22 września 1236 pomiędzy zakonem kawalerów mieczowych a pogańskimi Zemgalami i Żmudzinami oraz posiłkami innych Bałtów .
W bitwie tej poległo ok. 50-60 rycerzy zakonu , w tym jego mistrz krajowy Wolkwin .
Około 2700 zakonników nigdy nie powróciło z krucjaty , a ich losy są nieznane .
Była to pierwsza w takiej skali klęska kawalerów mieczowych .
Zakon kawalerów mieczowych , pierwszy katolicki zakon rycerski na ziemiach ludów bałtyckich , został poważnie osłabiony , niedługo potem , w 1237 , jego pozostali przy życiu członkowie zgodzili się na wcielenie do zakonu krzyżackiego .
Wynik bitwy skłonił podbite uprzednio przez nich ludy : Kurów , Zemgalów i Zelów , do powstania .
Efekty trwającego ponad 30 lat podboju ziem leżących na lewym brzegu Dźwiny zostały zniweczone .
Tło historyczne .
Zakon kawalerów mieczowych powstał w 1202 w Rydze w celu podboju ludów bałtyckich i ich chrystianizacji .
W latach 30 .
XIII wieku jego mistrzem został Volquin ( Wolkwin ) .
Zakon borykał się z problemami niedostatku funduszy , ludzi , a także złą reputacją .
Kawalerowie mieczowi byli w kwestii terenów dzisiejszej Estonii skonfliktowani z papieżem i Świętym Cesarstwem Rzymskim , swymi największymi sprzymierzeńcami .
Z końcem 1236 roku , gdy przybyli rycerze z Holsztynu , Wolkwin zażądał powierzenia mu dowództwa w bitwach .
Mistrz dowodził kampanią przy asyście księcia Pskowa , podejmując wyprawę na południe przeciwko pogańskiej Żmudzi .
Wcześniej tego samego roku zakon otrzymał bullę papieską , obwieszczającą krucjatę przeciwko Żmudzi i Litwie .
Przebieg bitwy .
Rycerze najechali na kilka osad Żmudzinów .
Wracając na północ , natknęli się jednakże przy brodzie na ich grupę , zdeterminowaną do walki .
Kawalerowie mieczowi nie chcieli podejmować ryzyka utraty koni poprzez utopienie ich w bagnach , a rycerze z Holsztyna odmówili walki pieszej , wymuszając rozbicie na noc obozowiska .
Następnego ranka , w dzień św .
Maurycego , przybyły do obozu główne siły Żmudzinów oraz Litwinów , prawdopodobnie pod dowództwem księcia Wikinta .
Lekko opancerzone siły pogan zmusiły do ucieczki lżej opancerzonych zakonników , podczas gdy rycerze w pełnym rynsztunku , w tym i Wolkwin , polegli .
Ci z rycerzy , którzy próbowali uciec do Rygi , rzekomo także zostali zabici przez pogan .
Dokładne miejsce bitwy pozostaje nieznane .
`` Chronicum Livoniae '' napisana przez Hermanna de Wartberge mówi o bitwie na `` terram Sauleorum '' .
Prawdopodobnie to okolice Szawli lub , co mniej prawdopodobne , miasteczko Vecsaule niedaleko Bowska , obecnie południowa Łotwa .
`` Saule/Saulė '' oznacza Słońce w obydwu językach : litewskim i łotewskim .
