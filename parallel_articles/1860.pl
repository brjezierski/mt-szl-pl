Biên Hoà
Biên Hoà – miasto w południowym Wietnamie , nad rzeką Đồng Nai , 30 km na północny wschód od Ho Chi Minh , ośrodek administracyjny prowincji Đồng Nai .
W 2013 roku liczyło 471 059 mieszkańców .
W mieście rozwinął się przemysł chemiczny , elektrotechniczny , papierniczy , spożywczy oraz rzemieślniczy .
