Biała Cerkiew ( stacja kolejowa )
Biała Cerkiew ( ukr : `` Станція Біла Церква '' ) – stacja kolejowa w Białej Cerkwi , w obwodzie kijowskim , na Ukrainie .
Jest częścią administracji Kolei Południowo-Zachodniej .
Znajduje się na linii Fastów – Mironówka .
Historia .
Stacja kolejowa w Białej Cerkwi został zbudowany w 1876 roku podczas budowy linii kolejowej łączącej Fastów z Mironówką , co znacznie przyspieszyło rozwój miasta .
Pod koniec XIX w i na początku XX wieku stacja Biała Cerkiew zajmowała drugie miejsce w obwodzie kijowskim w zakresie ruchu pasażerskiego po Kijowie i pod względem ruchu towarowego .
W 2007 roku stacja została zmodernizowana .
