Biłgoraj
Biłgoraj ( , , , ) – miasto w południowo-wschodniej Polsce , w województwie lubelskim , siedziba powiatu biłgorajskiego .
Drugie pod względem liczby ludności ( po Zamościu ) miasto w południowej części województwa lubelskiego .
W przeszłości był miastem szlacheckim , założonym przez Gorayskich herbu Korczak w drugiej połowie XVI wieku .
Biłgoraj leży w historycznej Małopolsce , w drugiej połowie XVIII wieku jako miasto prywatne położony był w powiecie urzędowskim w ziemi lubelskiej województwa lubelskiego .
Początkowo był istotnym ośrodkiem protestanckim .
