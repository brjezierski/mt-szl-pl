Biłohirja
Biłohirja ( ukr .
Білогір ' я ) , dawniej Lachowce ( Ляхівці ) – osiedle typu miejskiego na Ukrainie , w obwodzie chmielnickim , w rejonie szepetowskim , nad Horyniem .
W 2001 liczyło 5709 mieszkańców .
Znajduje tu się stacja kolejowa Suchowola oraz przystanek kolejowy Biłohirja , położone na linii Szepetówka – Tarnopol .
Historia .
Miejscowość powstała w 1441 , na prawie magdeburskim od 1583 .
Aleksander Jarmoliński herbu Korczak zaślubił Bohdannę , czyli Deodatę , córkę podobno Daszka Kalenikowicza , dziedzica Lachowiec .
Jej siostra była żoną Jaśka Sieniuty , stąd między ich potomkami ciągłe procesy o dobra lachowieckie .
W 1960 miejscowość uzyskała status osiedla typu miejskiego .
Do 2020 siedziba rejonu biłohirskiego .
