Białkowo ( rejon szarkowszczyński )
Białkowo – dawny zaścianek .
Tereny na których leżał znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie szarkowszczyńskim , w sielsowiecie Jody .
Historia .
W czasach zaborów w gminie Jody , w powiecie dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
W latach 1921–1945 zaścianek leżał w Polsce , w województwie wileńskim , w powiecie dziśnieńskim ( od 1926 w powiecie brasławskim ) , w gminie Jody .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 13 osób , 1 była wyznania rzymskokatolickiego a 12 prawosławnego .
Jednocześnie 1 mieszkaniec zadeklarował polską a 12 białoruską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 3 domach zamieszkiwało 18 osób .
Wierni należeli do parafii rzymskokatolickiej w Zamoszach i prawosławnej w Jodach .
Miejscowość podlegała pod Sąd Grodzki w Szarkowszczyźnie i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Jodach .
