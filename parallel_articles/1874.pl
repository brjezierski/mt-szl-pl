Położenie .
Biecz leży w Obniżeniu Gorlickim na Pogórzu Karpackim w południowej części Polski , w odległości ok. 35 km od granicy ze Słowacją oraz ponad 100 km od granicy z Ukrainą .
Powierzchnia miasta wynosi 17,71 km² ( 1 stycznia 2009 ) .
Biecz zajmuje ok. 18 % powierzchni gminy oraz ok. 1,8 % powierzchni powiatu gorlickiego .
