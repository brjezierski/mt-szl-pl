Biecz
Biecz – miasto w południowo-wschodniej Polsce , w województwie małopolskim , w powiecie gorlickim , siedziba gminy miejsko-wiejskiej Biecz .
Leży nad rzeką Ropą , na jednym ze wzgórz Pogórza Karpackiego .
Ze względu na bogatą historię często jest nazywane „ perłą Podkarpacia ” lub „ małym Krakowem ” .
Bywa także określany jako „ polskie Carcassonne ” , dzięki zachowanym fragmentom średniowiecznych murów miejskich i zabudowy .
Według danych GUS z 31 grudnia 2019 r. miasto miało 4607 mieszkańców .
Był miastem królewskim Korony Królestwa Polskiego .
Miasto królewskie starostwa bieckiego w powiecie bieckim województwa krakowskiego w końcu XVI wieku .
Biecz uzyskał prawo składu w 1616 roku .
Do połowy XVI wieku było jednym z największych w Polsce .
Przeżyło rozkwit w XIV i XV wieku dzięki posiadaniu statusu miasta królewskiego ; od XVII w. zaczęło podupadać .
Współcześnie jest niewielkim turystycznym miasteczkiem o malowniczym położeniu z licznymi zabytkami .
W Bieczu działają zakłady przemysłu metalowego i drzewnego .
Ma stację kolejową .
