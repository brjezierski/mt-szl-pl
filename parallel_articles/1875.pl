Biedrzychowa
Biedrzychowa ( niem .
`` Friedrichswalde '' ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie polkowickim , w gminie Polkowice .
Została założona w 1770 roku .
Podział administracyjny .
Miejscowość leży w województwie dolnośląskim , w powiecie polkowickim , w gminie Polkowice .
W latach 1975–1998 miejscowość administracyjnie należała do województwa legnickiego .
Położenie .
Biedrzychowa leży na południe od Polkowic .
Wieś leży przy drodze leśnej do Dąbrowy .
Toponimia .
Niemiecka nazwa wsi ( Friedrichswalde ) jest związana z legendą , jakoby przejeżdżający traktem król pruski Fryderyk II miał odpocząć pod jednym z rozłożystych świerków stojących na skraju lasu przy drodze .
