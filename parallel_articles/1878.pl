Bielany-Jarosławy
Bielany-Jarosławy – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Bielany .
Leży nad Starą Rzeką .
Zaścianek szlachecki Jarosławy należący do okolicy zaściankowej Bielony , położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni kościoła rzymskokatolickiego należą do parafii Trójcy Przenajświętszej w Rozbitym Kamieniu .
