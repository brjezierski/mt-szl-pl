Bielany-Żyłaki
Bielany-Żyłaki – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Bielany .
Leży przy trasie drogi krajowej nr 63 , nad Starą Rzeką .
Zaścianek szlachecki Żyłaki należący do okolicy zaściankowej Bielony , położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
Miejscowość była przejściowo siedzibą gminy Kowiesy .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni wyznania Rzymskokatolickiego zamieszkali we wsi należą do parafii Trójcy Przenajświętszej w Rozbitym Kamieniu .
Miejscowość jest siedzibą gminy Bielany .
Wierni kościoła rzymskokatolickiego należą do parafii Trójcy Przenajświętszej w Rozbitym Kamieniu .
Historia .
Pierwsze wzmianki pochodzą z 1489 , gdy Mikołaj Żyłak z Suchożebrów kupił grunty pod osadę od Mikołaja Szalany i jego syna Jana .
Na zakupionych gruntach Żyłak założył wieś nazwaną później Żylakami .
Wieś miała charakter głównie drobnoszlachecki .
Zamieszkiwali tu przede wszystkim Bielińscy , ale też inne rodziny szlacheckie i nieliczne chłopskie .
Ok. 1867 miejscowość stała się siedzibą gminy Kowiesy , prawdopodobnie ze względu na obecność lokalu nadającego się na urząd .
W 1921 roku w Bielanach-Żyłakach odnotowano 18 domów i 133 mieszkańców .
Znajdował się tam również posterunek policji państwowej .
