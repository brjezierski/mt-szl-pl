Bielowy
Bielowy – wieś w Polsce położona w województwie podkarpackim , w powiecie dębickim , w gminie Pilzno .
Leży w dolinie Wisłoki nad potokiem Bielawka .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnowskiego .
W miejscowości znajduje się kaplica pod wezwaniem Bożego Miłosierdzia należąca do Parafii św .
