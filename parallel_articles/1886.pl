Bierawka
Bierawka , Birawka , dawniej również `` Birowa '' – rzeka w południowej Polsce , prawy dopływ Odry o długości 55,5 km i powierzchni dorzecza 394 km² .
Uchodzi do Odry w jej 82,3 km w pobliżu wsi Bierawa .
Rzeka bierze swój początek w Orzeszu przy ulicy Żwirki i Wigury , w okolicy huty szkła , na wysokości 310 m n.p.m. , płynie w kierunku północnego zachodu m.in .
przez Czerwionkę-Leszczyny i Knurów .
W swoim dalszym biegu przepływa przez duże obszary leśne pomiędzy Koźlem , Gliwicami , Rybnikiem i Raciborzem , gdzie jej siła wody była wykorzystywana od XVIII wieku w kuźniach .
W pisowni polskiej nazwa rzeki występowała jako Birawka lub Bierawa .
Według językoznawcy Henryka Borka jest nazwą przedsłowiańską , zawierającą stary indoeuropejski temat bher- 'płynąć , ciec ' , który przyswojono za pomocą równie starej końcówki wodnej -awa .
Możliwe , że jest to nazwa germańska ( przed przybyciem Słowian zamieszkiwali tu prawdopodobnie Wandalowie ) z typową końcówką - ( a ) hwa 'rzeka , woda ' .
Wówczas pierwotna nazwa brzmiałaby Berahwa .
W latach 1936-1945 niemiecka nazwa rzeki brzmiała `` Bierau '' ( poprzednio `` Birawka '' ) .
