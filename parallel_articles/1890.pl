Bierzglin
Bierzglin – wieś w Polsce położona w województwie wielkopolskim , w powiecie wrzesińskim , w gminie Września .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
Historia .
Podczas okupacji nazwa wsi została zmieniona przez administrację niemiecką na `` Raymannsruh '' .
W ten sposób chcieli oni oddać hołd Helmuthowi Raymannowi , rolnikowi niemieckiemu mieszkającemu w Bierzglinie , który nie posiadał obywatelstwa niemieckiego .
Według przekazów ustnych , po wybuchu II wojny światowej , volksdeutsch wysyłał sygnały drogą radiową lub sygnały świetlne dla niemieckiego lotnictwa .
Został on przyłapany na sabotażu i rozstrzelany na mocy sądu wojskowego 1 września 1939 przez Wojsko Polskie II RP .
Po zajęciu Polski przez III Rzeszę hitlerowcy uznali takie osoby za bohaterów .
