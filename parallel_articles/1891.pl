Historia .
Obok Bierunia znajdowała się antyczna osada Leucaristus .
Została ona odwzorowana na antycznej mapie Klaudiusza Ptolemeusza z lat 142–147 naszej ery .
O tym , że miejscowość ta znajdowała się w sąsiedztwie Bierunia , informuje skorowidz nazw historycznych ORBIS LATINUS i wynika to z położenia wśród innych zidentyfikowanych miejscowości Śląska .
W X–XI w. istniał tu otoczony palisadą i niewątpliwie posiadający wieżę obserwacyjną gródek obronny mający za zadanie chronić kupców przeprawiających się przez rzekę .
Pozostałość – bieruński kopiec na szczycie którego stoi obecnie kapliczka św .
Jana Nepomucena , z XIX wieku .
W średniowieczu na pobliskiej Orlej Górce prawdopodobnie oddawano też hołd starosłowiańskiemu bogu Perunowi .
Bieruń otrzymał prawa miejskie w 1387 r. od księcia opawsko-raciborskiego Jana II Żelaznego .
Akt ten polegał na nadaniu praw wójtowskich Kusowiczowi za jego długoletnią wierną służbę .
O istnieniu na obszarze miasta osadnictwa świadczą wzmianki zamieszczone w dokumentach , na przykład o Ścierniach z 1272 r. , Bijasowicach z 1360 r. , Bieruniu z 1376 r. Do 1551 r. własność Piastów górnośląskich , od 1327 r. pod zwierzchnictwem Czech jako lenno .
Od 1743 r. w państwie pruskim i utrata praw miejskich .
Od 1865 r. ponownie miasto i zmiana nazwy na Bieruń Stary ( `` Alt Berun '' ) .
Od 1922 r. ponownie w granicach Polski .
W czasie II wojny światowej wcielony do Niemiec .
W latach 1945–1954 istniała wiejska gmina Bieruń Nowy .
W latach 1973–1975 miasto było siedzibą gminy wiejskiej Bieruń Stary .
W latach 1973–1991 dzielnica Tychów .
Od 1991 r. ponownie samodzielne miasto ( powtórnie pod nazwą Bieruń ) .
Od odzyskania samodzielności przez Bieruń datuje się konflikt między tym miastem a sąsiednimi Tychami o tereny fabryczne Fiat Auto Poland ( b. FSM ) oraz osiedle zbudowane przy tej fabryce .
Obecnie osiedle leży już w granicach administracyjnych Bierunia , natomiast sama fabryka pozostaje w Tychach .
Historycznie sporny teren należał do Bierunia , jednakże pozostawienie go w granicach Tychów było warunkiem zgody tego miasta na odłączenie Bierunia .
Spór odżył w 2010 r. , bieruńscy samorządowcy domagali się od Tychów zwrotu 123 ha terenów fabrycznych , na których działa kilka spółek-córek firmy Fiat Auto Poland .
Tychy zaproponowały korektę granicy o 123 ha na korzyść Bierunia , lecz w innym miejscu , na obszarze niezabudowanym .
Roszczenia Bierunia negatywnie zaopiniował wojewoda śląski .
W 1993 r. po raz pierwszy odbył się w Bieruniu I Festiwal Filmów Nieprofesjonalnych im .
Leona Wojtali .
Został on zorganizowany przy pomocy KWK Piast .
W 2005 r. odbyła się siódma edycja tego festiwalu , gdzie zanotowano rekordową ilość uczestników z całej Polski .
Gminę nawiedzają powodzie w 1997 i 2010 r .
Staraniem władz lokalnych , w tym z dużym zaangażowaniem finansowym , w dniu 1 października 2006 r. oddano do użytku nowo wybudowany budynek i uruchomiono nową jednostkę organizacyjną – Komendę Powiatową Policji o składzie osobowym 109 policjantów .
Nazwa .
Nazwa miejscowości wywodzi się od polskich zwrotów `` „ brać , pobierać ” '' i wiąże się z poborami podatkowymi .
Niemiecki nauczyciel Heinrich Adamy w swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 r. we Wrocławiu jako najstarszą nazwę wymienia `` Biernia '' podając jej znaczenie `` „ Steuerort ( Abgaben ) ” '' , czyli w języku polskim `` „ Wieś podatków ( pobór opłat , podatków ) ” '' .
Nazwa została później fonetycznie zgermanizowana na `` Berun '' w wyniku czego utraciła ona swoje pierwotne znaczenie .
Od chwili zaistnienia , aż do obecnych czasów , czyli do odzyskania samodzielności prawnej w 1991 r. , używano jednej nazwy w wymowie Bieruń .
W zależności od reguł pisowni używano nazw Bieron , Bierun .
W źródłach historycznych nazwa osady „ de Beruna ” użyta została dopiero w 1376 r. Górny Śląsk znajdował się wówczas pod panowaniem czeskich Przemyślidów .
Pisano też o nazwie `` Boruń '' .
Nazwę miejscowości w zlatynizowanej staropolskiej formie `` Byerun '' wymienia w latach 1470–1480 Jan Długosz w księdze `` Liber beneficiorum dioecesis Cracoviensis '' .
Na mapie Abrahama Orteliusa z 1603 r. miejscowość widnieje pod nazwą `` Bierun '' .
W statystycznym opisie Prus z 1837 r. występuje jedynie nazwa `` Berun '' .
Polską nazwę `` Bieruń '' w książce „ Krótki rys jeografii Szląska dla nauki początkowej ” wydanej w Głogówku w 1847 r. wymienił górnośląski pisarz , Józef Lompa .
Słownik geograficzny Królestwa Polskiego wydany pod koniec XIX w. podaje dwie polskie nazwy miejscowości – `` Beruń '' i `` Bieruń '' oraz niemiecką `` Berun '' .
