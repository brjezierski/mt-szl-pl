Bieruń
Bieruń ( niem .
`` Berun '' ) – miasto w Polsce , historycznie na Górnym Śląsku , położone w województwie śląskim , siedziba władz powiatu bieruńsko-lędzińskiego .
Według danych GUS z 31 grudnia 2021 r. miasto miało 19 087 mieszkańców .
Położenie .
Bieruń leży w południowej części Polski , przy granicy województwa śląskiego i małopolskiego , którą wyznacza Wisła .
Centrum miasta wyznaczają współrzędne geograficzne : 50º 05 ' szerokości geograficznej północnej i 19º 05 ' długości geograficznej wschodniej .
Miasto oddalone jest w linii prostej : 20 km od śródmieścia Katowic , 6 km od centrum Tychów , 10 km od centrum Oświęcimia , 61 km od centrum Krakowa i 273 km od centrum Warszawy .
Bieruń graniczy z miastami : Tychy i Lędziny i z gminami : Chełm Śląski , Chełmek , Bojszowy i Oświęcim .
Teren miasta położony jest na pograniczu Pagórów Jaworznickich , Doliny Górnej Wisły i Równiny Pszczyńskiej .
Historycznie Bieruń leży na Górnym Śląsku .
Według danych z 1 stycznia 2013 r. powierzchnia miasta wynosiła 40,5 km² , czyli 25,6 % powierzchni powiatu , co stawia to miasto na 106. pozycji w Polsce pod względem powierzchni .
