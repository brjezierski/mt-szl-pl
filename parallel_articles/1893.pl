Utworzenie powiatu ( 1972 ) .
Przygotowania do reformy administracyjnej ( 1975 ) , wprowadzającej w PRL 49 województw , skutkowały utworzeniem 1 listopada 1972 roku eksperymentalnego powiatu bieszczadzkiego .
Był to największy powiat w Polsce o powierzchni 2909 km² .
Siedzibą powiatu , mimo protestów większych Ustrzyk Dolnych , zostało Lesko .
Utworzenie powiatu bieszczadzkiego motywowano `` celem stworzenia właściwych warunków dla dalszego rozwoju gospodarczego całego regionu bieszczadzkiego ze szczególnym uwzględnieniem potrzeb stale rozwijającej się turystyki oraz celem lepszego zaspokajania potrzeb gospodarczych socjalno-kulturalnych zamieszkałej tam ludności '' .
