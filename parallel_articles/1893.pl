Gminy w miejsce gromad ( 1973 ) .
Zaledwie dwa miesiące po utworzniu powiatu nastąpiła likwidacja gromad i osiedli , a w ich miejsce utworzono gminy jako podstawowe jednostki podziału administracyjno-gospodarczego na terenach wiejskich .
