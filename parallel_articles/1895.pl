Bielinek kapustnik
Bielinek kapustnik ( `` Pieris brassicae '' ) – gatunek motyla z rodziny bielinkowatych .
Opis .
Motyl o rozpiętości skrzydeł 49–63 mm .
U obu płci tło wierzchu skrzydeł białe , na wierzchołku skrzydeł przednich czarna łukowata plama sięgająca w tył do pierwszej żyłki kubitalnej lub nieco poniżej niej , a przy przednim brzegu skrzydeł tylnych czarna plama .
Samica obok podanego wzoru ma jeszcze dwie okrągłe plamy i jedną podługowatą na każdym skrzydle przednim – wszystkie barwy czarnej .
Spód skrzydeł u obu płci bardzo podobny : skrzydło przednie białe z zielonkawożółtym przednim wierzchołkiem i dwiema czarnymi plamami pośrodku , tylne zaś zielonkawożółte z czarnym przyprószeniem na tle .
Ciało gąsienic jest niebieskawozielone , czarno plamkowane i opatrzone żółtą pręgą w części grzbietowej .
Barwa poczwarek jest zielonkawożółta z czarnym nakrapianiem .
Biologia i ekologia .
Występuje w lasach , na polach , w ogrodach i sadach .
Gąsienice żerują na roślinach z rodziny krzyżowych ( Brassicaceae ) takich jak kapusta , gorczyca polna , rzodkiew świrzepa , a także nasturcja .
Może być szkodnikiem ogrodów warzywnych .
Okres lotu .
W warunkach klimatycznych Europy Środkowej rocznie rozwijają się 2 pokolenia ( maj – czerwiec i lipiec – wrzesień ) , rzadziej 3 ( dodatkowe w październiku ) .
Gąsienice pierwszego pokolenia prawdopodobnie preferują żerowanie na dzikich roślinach z rodziny `` Brassicaceae '' .
Rozprzestrzenienie .
Szeroko rozprzestrzeniony w prawie całej Palearktyce .
