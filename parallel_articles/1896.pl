Biesiekierz
Biesiekierz ( niem .
`` Biziker '' ) – wieś w północno-zachodniej Polsce , położona w województwie zachodniopomorskim , w powiecie koszalińskim i gminie Biesiekierz , której jest siedzibą .
Według danych z 31 grudnia 2008 r. wieś miała 998 mieszkańców .
Przez wieś prowadzi droga wojewódzka nr 112 .
Historia .
Wieś Biesiekierz od XVI w. stanowiła część majątku kołobrzeskiego rodu Holkenów , których główna siedziba mieściła się w pobliskim Nosowie .
Następnie , aż do XIX wieku , wieś wielokrotnie zmieniała właścicieli .
Pod koniec XVIII w. należała do rodziny von Kameke , a ostatnim właścicielem był Edward graf von Herztberg .
Komisja Ustalania Nazw Miejscowości przyjmując nazwę nawiązała do wyrazu „ kierz ” ( krzak ) oraz słowa „ biesi ” ( „ diabelski ” ) lub „ byczy ” , ewentualnie do miejsca pozbawionego krzewów , czyli „ bezkrza ” .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Biesiekierz .
Infrastruktura .
Biesiekierz jest głównym ośrodkiem gminy .
Zlokalizowany jest tu urząd gminy , biblioteka , posterunek policji , Centrum Zdrowia i Opieki Społecznej , klub wiejski , szkoła podstawowa , przedszkole , filia urzędu pracy i boisko gminne , z którego korzysta klub piłkarski `` Wybrzeże '' .
W miejscowości ma remizę jednostka Ochotniczej Straży Pożarnej , włączona do krajowego systemu ratowniczo-gaśniczego .
Atrakcje turystyczne .
Na terenie wsi znajduje się kościół pod wezwaniem Chrystusa Króla z XIV wieku .
We wnętrzu kościoła znajduje się późnorenesansowy ołtarz oraz chrzcielnica z XVII w. fragment nastawy ołtarzowej z datą renowacji ( 1932 ) , a także dwie rzeźby barokowe oraz dzwon gotycki .
W Biesiekierzu położony jest zespół pałacowo-parkowy z przełomu XIX i XX wieku , którego powierzchnia wynosi 2,5 hektara .
Biesiekierska aleja dębowa , licząca 9 drzew , jest pomnikiem przyrody .
Biesiekierz stolicą ziemniaka .
Nieopodal pałacu od lat 80 .
XX wieku znajduje się Pomnik Ziemniaka mierzący ze stelażem 9 metrów , gdzie sama bulwa ma 3,95 m. Położony jest kilkanaście metrów na południe od dawnej trasy drogi krajowej nr 6 ( obecnie droga wojewódzka nr 112 ) .
Według Urzędu Gminy Biesiekierz jest to prawdopodobnie największy na świecie monument ziemniaka .
Został on utworzony na cześć nowej odmiany tej rośliny , która została wyhodowana w pobliskiej Hodowli Ziemniaka w Biesiekierzu .
Pomysłodawcą całego przedsięwzięcia był dyrektor Stacji Hodowli Roślin Mariusz Roeder .
Jego autorem jest Wiesław Adamski .
Prace nad pomnikiem trwały blisko rok , natomiast jego oficjalne odsłonięcie nastąpiło w 1983 roku .
