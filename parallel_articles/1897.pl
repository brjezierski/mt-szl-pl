Bieździedza
Bieździedza ( daw .
`` Bezdochoici '' , `` Beszdzyedza '' , `` Biezdziedza '' ) – wieś w Polsce położona w województwie podkarpackim , w powiecie jasielskim , w gminie Kołaczyce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krośnieńskiego .
Miejscowość jest siedzibą parafii pod wezwaniem Świętej Trójcy , należącej do dekanatu Brzostek , diecezji rzeszowskiej .
Geografia .
Od wschodu sąsiaduje z Bieździadką , od zachodu z Kołaczycami i Nawsiem Kołaczyckim , od północy z Sowiną .
Przez miejscowość przebiega droga powiatowa Kołaczyce – Lubla .
Przysiółek Rzeki położony jest przy głównej drodze do Sowiny .
