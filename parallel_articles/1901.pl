Biała Niżna
Biała Niżna – wieś w Polsce położona w województwie małopolskim , w powiecie nowosądeckim , w gminie Grybów .
Wieś królewska `` Biała '' , położona w drugiej połowie XVI wieku w powiecie sądeckim województwa krakowskiego , należała do tenuty grybowskiej .
W latach 1975–1998 miejscowość administracyjnie należała do województwa nowosądeckiego .
Położenie .
Biała Niżna znajduje się w dolinie rzeki Biała .
Zabudowania i pola miejscowości zajmują płaskie i dość rozległe dno doliny Białej oraz okoliczne wzgórza , między innymi Matelankę .
Znajduje się w dwóch regionach geograficznych : na Pogórzu Rożnowskim ( po lewej stronie Białej ) i Pogórzu Ciężkowickim ( po prawej stronie Białej ) .
Graniczy z miejscowościami : Grybów , Siołkowa , Stróże , Gródek oraz Chodorowa .
