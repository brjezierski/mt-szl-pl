Biała Piska
Biała Piska ( , 1938–1945 ) – miasto w Polsce , w województwie warmińsko-mazurskim , w powiecie piskim , siedziba gminy miejsko-wiejskiej Biała Piska .
Pod względem historyczno-etnograficznym Biała Piska leży na Mazurach , na obszarze dawnej Galindii .
Historia .
Wieś Biała Piska powstała w 1428 roku w miejscu wcześniejszej osady pruskiej `` Gayle '' .
Nazwa pochodzi z języka staropruskiego – `` gaylis '' oznacza biały .
Koloniści z Mazowsza przetłumaczyli jako `` Biała '' .
Wieś wymieniana w sprawozdaniu wizytacyjnym z 1424 roku jako licząca 60 łanów z młynem , w dąbrowie przy Gayle ( dąbrowa położona pół mili od Jeziora Kumielskiego ) , gdzie niejaki Cwalina wraz z 20 krewnymi objął obszar 100 łanów .
Dobra te znajdowały się ćwierć drogi od młyna zwanego `` Steffanflies '' .
Przywilej lokacyjny dla wsi czynszowej wystawiony został 9 października 1428 roku przez `` Josta Struppergera '' komtura bałgijskiego i wójta natangijskiego , za wiedzą i wolą wielkiego mistrza `` Pawła von Rusdorfa '' .
Wieś położona była w okolicy zwanej `` Gayle '' , na 60 łanach na prawie chełmińskim .
Sołtys i zasadźca – `` Piotr Szulc '' otrzymał 6 łanów chełmińskich , 4 łany przeznaczono na uposażenie parafii .
