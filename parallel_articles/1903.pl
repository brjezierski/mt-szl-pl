Położenie .
W podziale fizycznogeograficznym Biała Podlaska znajduje się na pograniczu Niziny Południowopodlaskiej i Polesia Zachodniego .
Granica tych makroregionów biegnie po lewej stronie Krzny , wzdłuż łagodnej krawędzi wysoczyzny Równiny Łukowskiej , gdzie lokowano ośrodek miejski .
Południowe części miasta wraz z doliną Krzny leżą w Zaklęsłości Łomaskiej .
Według danych z 1 stycznia 2009 powierzchnia miasta wynosi 49,4 km² i jest największa spośród miast Podlasia Południowego .
