Biała Podlaska
Biała Podlaska – miasto na prawach powiatu we wschodniej Polsce , w województwie lubelskim , siedziba powiatu bialskiego i gminy Biała Podlaska .
Biała Podlaska uzyskała lokację miejską przed 1498 rokiem .
Miasto magnackie położone było w końcu XVIII wieku w hrabstwie bialskim w powiecie brzeskolitewskim województwa brzeskolitewskiego .
Miasto prywatne Królestwa Kongresowego , położone było w 1827 roku w powiecie bialskim , obwodzie bialskim województwa podlaskiego .
