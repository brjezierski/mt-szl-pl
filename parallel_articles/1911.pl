Język białoruski
Język białoruski ( biał .
, ) – język wschodniosłowiański używany głównie na Białorusi .
Liczba osób posługujących się nim wynosi około 6,5 miliona .
Język białoruski wyodrębnił się z języka ruskiego w XIV wieku .
Białoruski język literacki powstał w XIX wieku , zaś literatura piękna w tym języku rozwinęła się w XX wieku ; literatura białoruska nie jest tożsama z literaturą w języku ruskim , z którego wyodrębnił się zarówno język białoruski , jak i język ukraiński .
Język białoruski , podobnie jak wiele innych elementów składających się na tożsamość narodową Białorusinów , wypierany jest w procesie rusyfikacji Białorusi przez język rosyjski .
Historia .
Okresy w historii języka białoruskiego :
W XIII wieku język ruski stał się językiem państwowym Wielkiego Księstwa Litewskiego .
Pierwsza drukowana książka w tym języku wydana została przez Franciszka Skarynę w 1517 roku .
Wydając w latach 1517–1519 księgi Starego Testamentu po rusku ( Biblia Franciszka Skaryny ) , był on autorem drugiego na świecie tłumaczenia Biblii na język niekanoniczny .
W XVI wieku jedna z pierwszych kodyfikacji prawa w Europie – tzw .
statuty litewskie ( 1529 , 1566 , 1588 ) – pisane były po rusku .
Język Statutów Litewskich , odpowiadał mowie potocznej przeważającej części mieszkańców ówczesnego państwa litewskiego , miał podstawę słowiańską , zbliżoną do staropolskiego i starobiałoruskiego , a najbardziej przypominał miejscową mowę , nazywaną obecnie mową „ prostą ” .
