, , `` Biełaruś '' , `` Respublika Biełaruś '' ; lub , ) – państwo w Europie Wschodniej .
Graniczy z Polską ( ze strony zachodniej ) , Litwą , Łotwą ( na północy ) , Rosją ( na wschodzie ) i Ukrainą ( na południu ) .
W odróżnieniu od swoich sąsiadów nie ma dostępu do morza .
Jest to największe pod względem powierzchni państwo śródlądowe w Europie .
Białoruska SRR była członkiem założycielem ONZ i członkiem 8 organizacji wyspecjalizowanych ONZ ; obok Rosyjskiej FSRR i Ukraińskiej SRR była jedną z trzech republik radzieckich będących podmiotem prawa międzynarodowego .
W 1991 Białoruś ogłosiła niepodległość .
Jest członkiem organizacji regionalnych i subregionalnych , tj .
WNP , EaWG , ZBiR i OBWE .
