Białowody
Białowody – wieś w Polsce położona w województwie lubelskim , w powiecie hrubieszowskim , w gminie Uchanie .
Demografia .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Wieś stanowi sołectwo gminy Uchanie .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 168 i była dziesiątą co do wielkości miejscowością gminy Uchanie .
Historia .
Białowody wieś z folwarkiem w powiecie hrubieszowskim , gminie Grabowiec , parafii Uchanie .
W 1827 roku było tu 19 domów i 105 mieszkańców .
