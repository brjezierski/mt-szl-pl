Biały Bór ( staropl .
`` Białobork '' , dawniej `` Ball de Olde '' , ) – miasto w Polsce , położone w województwie zachodniopomorskim , w powiecie szczecineckim , siedziba gminy miejsko-wiejskiej Biały Bór , historycznie na Pomorzu Gdańskim .
Położone jest w dolinie rzeki Białej , na przesmyku między jeziorami Bielsko i Ławiczka .
Prawa miejskie uzyskał w 1382 , w czasie II wojny światowej został znacznie zniszczony .
Według danych z 1 stycznia 2018 Biały Bór liczył 2 171 mieszkańców .
W miejscowości działało `` Stado Ogierów Biały Bór '' .
Od 1994 r. jako `` Zakład Treningowy Skarbu Państwa Biały Bór '' , potem ponownie jako SO .
