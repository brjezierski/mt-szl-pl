Bieg maratoński
Bieg maratoński ( maraton ) – bieg na dystansie 42 195 m .
Pochodzenie nazwy .
Nazwa pochodzi od miejscowości Maraton w Grecji .
Według Herodota po zwycięskiej dla Greków bitwie z Persami pod Maratonem w 490 r .
p.n.e. , armia perska zaokrętowała i wypłynęła w kierunku bezbronnych Aten .
Widząc to , Grecy udali się co sił w nogach do miasta , przybywając praktycznie równocześnie z okrętami perskimi .
Bieg ten stał się podstawą romantycznych historii , według których posłaniec Filippides pobiegł do Aten , by opowiedzieć o zwycięstwie i poinformować Ateńczyków , że płynie ku nim flota perska .
Po przekazaniu tej wiadomości padł martwy .
W 1879 r. brytyjski poeta Robert Browning napisał na kanwie tej historii znany wiersz `` Filippides '' ( ang .
`` Pheidippides '' ) .
Michel Bréal , francuski filolog , zainspirowany historią marszu zasugerował Pierre'owi de Coubertinowi , by włączył bieg na dystansie odpowiadającym odległości z Maratonu do Aten do programu pierwszych nowożytnych igrzysk olimpijskich w Atenach .
Dyscyplina olimpijska .
W pierwszych igrzyskach bieg maratoński był rozgrywany na dystansie 40 km .
W rzeczywistości dystans między Maratonem a Atenami wynosi 37 km , jednak postanowiono tę liczbę zaokrąglić .
Na igrzyskach w Londynie dystans zwiększono o 2195 metrów ( przesuwając metę w pobliże miejsca , gdzie na trybunach siedział brytyjski król , Edward VII ) .
Bieg maratoński mężczyzn tradycyjnie kończy każde igrzyska olimpijskie .
Maraton kobiet został włączony do programu igrzysk olimpijskich dopiero w 1984 roku w Los Angeles .
Masowy bieg uliczny .
Od przeszło 100 lat na ulicach największych miast świata rozgrywane są masowe biegi uliczne m.in .
na dystansie maratońskim .
W przeciwieństwie do igrzysk olimpijskich i mistrzostw świata , w maratonach ulicznych najczęściej udział może wziąć każdy .
W największych imprezach ( Londyn , Nowy Jork , Boston , Chicago , Wiedeń , Berlin ) uczestniczy przeszło 30 tys .
biegaczy zawodowców i amatorów .
Najstarszy polski maraton organizowany jest w Dębnie .
Dużą frekwencją jak na polskie warunki ( około 5000 uczestników ) cieszą się maratony : Poznań Maraton , Wrocław Maraton , Cracovia Maraton i Maraton Warszawski .
Najstarszym maratonem nowożytnym jest maraton w Bostonie ( pierwsza edycja tego maratonu odbyła się 19 kwietnia 1897 roku ) , a w Europie – w Koszycach .
Największe maratony odbywają się : w Europie – w Berlinie ( ponad 41 tys .
uczestników ) oraz w Londynie , a w Polsce – w Warszawie ( w 2013 r. 8673 uczestników ) .
Na większości maratonów obowiązuje limit czasowy pokonania dystansu wynoszący 5 lub 6 godzin .
