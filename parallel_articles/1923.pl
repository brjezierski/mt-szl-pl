Blachownia ( stacja kolejowa )
Blachownia – stacja kolejowa istniejąca od 1903 r. w Blachowni , w województwie śląskim , w Polsce , zbudowana wraz z linią kolejową Częstochowa-Herby Ruskie .
Historia .
Początkowo , w latach 1903-1911 była to linia wąskotorowa 1067 mm , w latach 1911-1915 szerokotorowa 1524 mm , w czasie I wojny światowej została przez Niemców przebudowana na normalnotorową 1435 mm .
Stacja Blachownia znajduje się na terenie dzielnicy Ostrowy i przed likwidacją położonej na odgałęzieniu stacji końcowej Blachownia nosiła nazwę Ostrowy , a w latach 1941-1945 nazwę Blachstädt .
Od tej stacji do zlikwidowanej stacji Blachownia prowadziło jednotorowe odgałęzienie , wykorzystywane jeszcze w latach 80 .
XX wieku do transportu towarowego do Huty Blachownia , ostatecznie zlikwidowane w 2003 r .
W 2015 PKP przeznaczyło budynek dworca do rozbiórki z powodu bycia zbędnym dla prowadzonej działalności .
W 2017 stacja obsługiwała 0–9 pasażerów na dobę .
