Black Sabbath
Black Sabbath – brytyjski zespół hardrockowy , główny prekursor gatunku heavy metal .
Grupa powstała w 1968 jako kwartet założony przez nastoletnich przyjaciół w Birmingham .
Jego pierwotny skład tworzyli : Anthony „ Tony ” Iommi , William „ Bill ” Ward , John „ Ozzy ” Osbourne i Terence „ Geezer ” Butler .
W ciągu pierwszych dziesięciu lat swej działalności grupa stała się jednym z najbardziej znaczących i rozpoznawalnych zespołów heavy metalowych .
W 1979 z grupy odszedł Ozzy Osbourne , który następnie rozpoczął karierę solową .
Po jego odejściu Black Sabbath kontynuował działalność ze zmiennym szczęściem , starając się zachować swoje oryginalne brzmienie mimo dalszych zmian personalnych .
Jedynym muzykiem , który pozostał w niej na przestrzeni lat z pierwotnego składu był Tony Iommi .
Zespół odrodził się , aby zagrać kilka koncertów w 1998 w pierwotnym składzie .
Grupa zakończyła oficjalnie działalność w 2017 .
Szacowany nakład sprzedanych płyt zespołu na całym świecie wynosi 75 mln egzemplarzy .
