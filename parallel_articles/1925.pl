Black metal
Black metal – jeden z podgatunków heavy metalu , powstały na początku lat 80 .
XX wieku w wyniku ewolucji thrash metalu .
W latach 80. określenie „ black metal ” odnoszono do wszystkich zespołów mających satanistyczne teksty i otoczkę , bez względu na styl muzyczny .
Black metal był czymś w rodzaju podgatunku występującego wśród ówczesnych gatunków metalu ( jak heavy , speed czy thrash metal ) , pionierem był brytyjski zespół Venom , który wprowadził do heavy metalu punkowe elementy i grał speed metalowo .
Inne zespoły to np .
szwedzki Bathory , szwajcarskie Hellhammer i Celtic Frost .
Później na przełomie lat 80. i 90. był to już odrębny gatunek operujący charakterystycznym „ skrzekliwym ” sposobem śpiewania i stylistyką opartą na czarno-białym makijażu typu corpse paint , zapoczątkowanym przez Kinga Diamonda z duńskiego Mercyful Fate .
W black metalu pojawił się za sprawą Deada ( wokalista Mayhem ) .
Zespoły „ drugiej fali ” to między innymi Darkthrone , Satyricon , Emperor .
Pierwszym zespołem drugiej fali black metalu był zaczynający jeszcze pod koniec lat 80 .
Mayhem , który zadebiutował w 1987 r. EP-ką „ Deathcrush ” .
Charakterystyka gatunku .
Black metal muzycznie bazuje na dźwiękach silnie przesterowanych , często przechodzących w tremolo , szybkich , szesnastkowych partii gitar oraz na szybkim rytmie perkusji ( tzw .
blast beats ) .
Wokaliści posługują się niskim , gardłowym , warczącym lub skrzeczącym głosem – growlem .
Brzmienie uzyskiwane przez zespoły tego gatunku jest surowe , posępne i złowrogie .
Utwory blackmetalowe mają nierzadko konstrukcję odbiegającą od tradycyjnych zasad kompozycyjnych z podziałem na zwrotki i refreny .
W warstwie tekstowej dominują motywy satanistyczne , antychrześcijańskie , pogańskie i okultystyczne .
Rzadziej pojawia się ateizm , nihilizm , depresja , mizantropia , tematyka wojenna czy apokaliptyczna .
Osobną grupę tworzą zespoły nawiązujące do ideologii narodowosocjalistycznej .
Jedną z form blackmetalowej ekspresji jest tzw .
corpse paint , rodzaj czarno-białego makijażu , stosowanego zarówno przez muzyków , jak i przez fanów na koncertach .
