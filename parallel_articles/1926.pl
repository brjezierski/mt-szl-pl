Blackburn ( West Lothian )
Blackburn – miasto w środkowej Szkocji , w hrabstwie West Lothian , położone nad rzeką Almond .
W 2011 roku liczyło 5338 mieszkańców .
W przeszłości miejscowość była ośrodkiem przemysłu włókienniczego .
W połowie XIX wieku rozwinęło się tu także wydobycie węgla .
Gwałtowny rozrost miasta nastąpił w latach 60 .
XX wieku .
Na północ od miasta biegnie autostrada M8 .
