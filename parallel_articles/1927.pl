Blackpool ( borough )
Blackpool – dystrykt typu unitary authority obejmujący większą część hrabstwa ceremonialnego Lancashire , w Anglii .
W 2011 roku dystrykt liczyła 142 065 mieszkańców .
W 1998 roku stał się unitary authority .
