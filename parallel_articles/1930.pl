Blaise Matuidi
Blaise Matuidi ( ur .
9 kwietnia 1987 w Tuluzie ) – francuski piłkarz pochodzenia angolskiego , który występował na pozycji pomocnika .
W grudniu 2022 roku zakończył karierę .
Kariera klubowa .
Blaise Matuidi urodził się w Tuluzie , w rodzinie pochodzenia angolańskiego .
Wychowywał się w Fontenay-sous-Bois koło Paryża .
Piłkarskie treningi rozpoczął w akademii piłkarskiej INF Clairefontaine , a następnie został zawodnikiem klubu US Créteil-Lusitanos .
Nie zaliczył jednak debiutu w pierwszym zespole , a w 2003 roku został piłkarzem Troyes AC .
23 listopada 2004 roku zadebiutował w jego barwach w Ligue 2 w wygranym 2–1 wyjazdowym meczu przeciwko FC Gueugnon .
W 2005 roku awansował z Troyes AC do Ligue 1 , a po awansie klubu stał się jego podstawowym zawodnikiem .
W sezonie 2005/2006 był jednym z najostrzej grających piłkarzy w Ligue 1 , gdy został ukarany 11 żółtymi kartkami .
W sezonie 2006/2007 spadł z Troyes AC do Ligue 2 .
Latem 2007 po spadku Troyes AC Blaise Matuidi przeszedł za 4 miliony euro do AS Saint-Étienne .
W nowym zespole także stał się członkiem wyjściowej jedenastki , a 11 sierpnia zaliczył swoje pierwsze spotkanie w koszulce „ Zielonych ” , wygrane 3–1 z Valenciennes FC .
W 2008 roku zajął z AS Saint-Étienne 5. miejsce w ligowej tabeli , najwyższe w dotychczasowej karierze .
W lipcu 2011 roku przeszedł do Paris Saint-Germain F.C. , gdzie ma być następcą Claude ’ a Makélélé na pozycji defensywnego pomocnika
W 2017 roku przeniósł się do Juventus F.C .
za kwotę około 20 milionów funtów .
Kariera reprezentacyjna .
W latach 2005–2007 Matuidi rozegrał 9 spotkań w reprezentacji Francji U-19 .
Z kolei w latach 2007–2008 występował w reprezentacji U-21 i zaliczył 19 występów będąc jej podstawowym zawodnikiem .
We wrześniu 2010 zadebiutował w pierwszej reprezentacji Francji .
Wraz ze tą reprezentacją w 2018 roku w Rosji został mistrzem świata .
