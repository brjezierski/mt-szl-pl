Blaison-Saint-Sulpice
Blaison-Saint-Sulpice – gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku populacja ludności wynosiła 1234 mieszkańców .
Gmina została utworzona 1 stycznia 2016 roku z połączenia dwóch wcześniejszych gmin : Blaison-Gohier oraz Saint-Sulpice .
Siedzibą gminy została miejscowość Blaison-Gohier .
