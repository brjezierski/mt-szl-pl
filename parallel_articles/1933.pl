Zymonema dermatitidis
Zymonema dermatitidis ( Gilchrist & amp ; W.R. Stokes ) C.W .
Dodge – gatunek grzyba z typu workowcow ( `` Ascomycota '' ) .
Grzyb pasożytniczy , u ludzi wywołujący chorobę .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Incertae sedis , Incertae sedis , Incertae sedis , Incertae sedis , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy , w 1898 roku , takson ten zdiagnozowali T. Caspar Gilchrist i W.R .
Stoke , nadając mu nazwę Blastomyces dermatitidis ( bazonim , dominujący w piśmiennictwie ) .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu Carroll William Dodge w roku 1935 .
