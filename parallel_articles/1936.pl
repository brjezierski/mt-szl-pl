Pęcherz
Pęcherz ( łac .
`` bulla '' ) – w dermatologii – wykwit wyniosły ponad powierzchnię skóry , o średnicy powyżej 0,5 cm .
Pęcherze to oddzielenie naskórka od skóry właściwej , wypełnione płynem limfatycznym .
Powstają na skórze stóp lub dłoni w wyniku otarć , np .
butów lub narzędzi , ustępują bez pozostawienia blizny .
`` Pęcherze '' mogą również występować na błonach śluzowych .
W piśmiennictwie angielskim na określenie pęcherzyków i `` pęcherzy '' istnieje określenie `` blister '' , niemające odpowiednika w nazewnictwie polskim .
