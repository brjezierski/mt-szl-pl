Włochatka jasna
Włochatka jasna ( `` Trametes trogii '' Berk . )
– gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Trametes , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimów łacińskich ma ponad 30 .
Niektóre z nich :
Polską nazwę nadał Władysław Wojewoda w 2003 r. , wcześniej gatunek ten opisywany był przez Stanisława Domańskiego jako brunatka Trogiusza , inne źródła wymieniają też nazwę brunatka kosmata .
Wszystkie te nazwy są jednak niespójne z obecną nazwą naukową , według Index Fungorum gatunek ten należy bowiem do rodzaju `` Trametes '' ( wrośniak ) .
Morfologia .
Jednoroczny , do podłoża przyrasta zazwyczaj bokiem , rzadko tylko jest rozpostarty .
Ma półokrągły , wydłużony kształt , szerokość do 12 cm , grubość 2-4 cm , na przekroju poprzecznym ma trójkątny profil ( podstawa przyrośnięta jest do podłoża ) .
Górna powierzchnia jest niestrefowana , lub lekko strefowana pokryta włoskami o długości 1-2 mm .
Mają białawy kolor , ale na szczycie szarawy lub rdzawobrązowy .
Młode owocniki owłosione są jednolicie , u starszych tworzą się kępki nadające owocnikowi szorstki wygląd .
