Lactarius aquizonatus
Lactarius aquizonatus Kytöv .
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Morfologia .
Średnicy 9–20 ( do 25 ) cm , o pokroju wypukłym , z wgłębieniem w środku lub lejkowatym , białawy lub żółtawo-ochrowy , z wodnistym , koncentrycznym strefowaniem , pokryty kosmkami .
Blaszkowy , blaszki białawe lub jasnołososiowe , proste , gęsto rozstawione i przyrośnięte do trzonu .
Długości 2,6–6 cm i średnicy 2–4,5 ( do 7 ) cm , żółtawy , pusty w środku .
Zbudowany jest z kulistawych komórek powodujących ich specyficzną kruchość i nieregularny przełam .
O ostrym smaku i słodkawej woni , wydzielający białe mleczko , przebarwiające się żółto na powietrzu , pomarańczowe w roztworze wodorotlenku potasu .
Elipsoidalne , o wymiarach 6,8–8,8×4,8–6,1 μm , pokryte amyloidalną siateczką .
Występowanie .
Organizm mikoryzowy , rozwijający się w glebie w wilgotnych lasach , w obecności świerku pospolitego ( `` Picea abies '' ) , sosny zwyczajnej ( `` Pinus sylvestris '' ) lub jałowca pospolitego ( `` Juniperus communis '' ) oraz brzóz ( `` Betula sp .
`` ) , szczególnie na wapiennych glebach .
Wytwarza owocniki ( w Europie ) w sierpniu i wrześniu .
Władysław Wojewoda nie podaje stanowisk tego grzyba w Polsce , jednak był on notowany w okolicach Bukowna .
