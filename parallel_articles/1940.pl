Helvella costifera
Helvella costifera Nannf .
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1953 r. John Axel Nannfeldt i nadana przez niego nazwa jest ważna do dzisiaj .
Synonimy :
Morfologia .
Składający się z kapelusza i trzonu .
Kapelusz o średnicy 1,5–4,5 cm , początkowo w kształcie miseczki z nieco podwiniętym brzegiem , potem coraz bardziej płaski z prostym brzegiem .
Górna powierzchnia o barwie od średnio do ciemnoszarobrązowej lub szarej , naga i gładka , dolna szarawa , czasami przy trzonie biaława , z rozwidlonymi , białawymi , tępymi , zaokrąglonymi żebrami , które od trzonu dochodzą czasami aż do brzegu kapelusza .
Trzon o wysokości 1–2 cm i grubości do 1 cm , rozszerzony w pobliżu kapelusza , głęboko żebrowany z tępo zakończonymi żebrami , sporadycznie przekształcającymi się w kieszenie .
