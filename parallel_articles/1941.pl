Piestrzenica olbrzymia
Piestrzenica olbrzymia ( `` Gyromitra gigas '' ( Krombh . )
Cooke ) – gatunek grzybów z rodziny krążkownicowatych ( `` Discinaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gyromitra , Discinaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1800 r. Julius Vincenz von Krombholz nadając mu nazwę `` Helvella esculenta '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1878 r. Mordecai Cubitt Cooke , przenosząc go do rodzaju `` Gyromitra '' .
Synonimy :
Morfologia .
Średnica 5–15 cm .
Jest nieregularnie , falisto lub mózgowato powyginana , o płatach przygiętych do nasady trzonu lub też mocno z nim zrośniętych .
Ma barwę odjasnooliwkowożółtej do ochrowożółtej , w miarę dojrzewania ciemnieje .
W środku pusta , biaława .
Biały albo bladoochrowy ; gruby , stosunkowo krótki , zwężający się ku podstawie , rzadziej nieco bulwkowaty , falisto bruzdowany , o gładkiej powierzchni ; komorowaty i pusty wewnątrz .
Ma długość od 1/5 do 1/4 wysokości główki i często taką samą jak ona szerokość .
