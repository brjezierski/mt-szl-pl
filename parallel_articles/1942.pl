Kierunkowskaz
Kierunkowskaz – mechaniczny ( dawniej ) lub świetlny ( obecnie ) sygnalizator planowanej zmiany kierunku jazdy lub pasa ruchu przez pojazd mechaniczny .
Historia kierunkowskazu .
W latach 20 .
XX wieku liczba samochodów - zarówno w Europie , jak i Ameryce Północnej - była już tak duża , że wypadki drogowe zaczęły być poważnym problemem .
Podstawową przyczyną takiego faktu były częste nieporozumienia pomiędzy kierowcami , co do obieranego kierunku jazdy .
Chęć skrętu sygnalizowało się wyciągnięciem ręki za `` burtę '' samochodu .
Było to dla kierowców bardzo niewygodne , a co gorsza słabo widoczne .
Pierwszy kierunkowskaz wprowadziła na rynek niemiecka firma Bosch w październiku 1928 roku .
Miał on postać unoszonego ramienia ze światełkiem odblaskowym lub lampką , w Polsce nazywany chorągiewką .
Obecnie tak dawny typ kierunkowskazu określa się jako `` kierunkowskaz ramieniowy '' .
Wynalazek ten momentalnie zyskał ogromną popularność .
Kierunkowskaz migowy , jaki znamy obecnie , na rynek wprowadziła firma Hella dopiero w 1951 roku .
Pierwszymi modelami samochodów z kierunkowskazami były Ford Taunus oraz Borgward Goliath .
O popularności pierwszej formy kierunkowskazu może świadczyć fakt , że był w produkcji jeszcze przez 10 lat od zaprezentowania `` następcy '' .
Początkowo umieszczany był tylko z tyłu i razem z reflektorami z przodu samochodu .
Współcześnie dodatkowy kierunkowskaz ( boczny ) umieszcza się także pomiędzy przednim kołem a przednimi drzwiami .
W ostatnim czasie popularne stało się również umieszczanie kierunkowskazów bocznych na lusterkach .
Wyjątkiem jest np .
Porsche 911 , które posiada dwie pary kierunkowskazów w przednim zderzaku , a nie ma żadnego między przednimi a tylnymi kołami .
