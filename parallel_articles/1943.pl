Gospodarka .
Bliski Wschód jest bardzo ważnym regionem gospodarczym , gdzie koncentruje się znaczna część światowego wydobycia ropy naftowej .
Ponadto krzyżują się tu ważne międzynarodowe linie komunikacji lotniczej i morskiej ( Kanał Sueski ) .
Pozostałe informacje .
Określenia Bliski , Środkowy i Daleki Wschód to typowy przykład nazwy etnocentrycznej , a w tym wypadku europocentrycznej , czyli z punktu widzenia Europejczyków .
Nazwa „ Bliski Wschód ” oznacza południowo-zachodnią Azję i północno-wschodnią Afrykę .
Obecnie w języku angielskim nazwa `` Near East '' jest wypierana przez `` Middle East '' ( Środkowy Wschód ) .
Badaniem starożytnych i nowożytnych , żywych i wymarłych kultur Bliskiego Wschodu zajmują się odpowiednie działy orientalistyki .
Bliski Wschód odgrywa duże znaczenie gospodarcze i strategiczne .
Leży na skrzyżowaniu trzech kontynentów : Europy , Azji i Afryki , przechodzą tędy ważne szlaki komunikacyjne z Europy i północy Afryki na Daleki Wschód .
Drogi morskie przebiegają przez cieśniny czarnomorskie , Morze Śródziemne , Kanał Sueski , Morze Czerwone i zatokę Perską , krzyżują się linie powietrzne łączące najważniejsze porty lotnicze Europy z południową Afryką , Azją , Dalekim Wschodem i Australią .
