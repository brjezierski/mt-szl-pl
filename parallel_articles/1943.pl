Bliski Wschód
Bliski Wschód ( arab .
الشرق الأوسط ; hebr .
המזרח התיכון ; per .
خاورمیانه , Xâvar-e Miyâne ; przestarzałe `` Lewant '' ) – region geograficzny obejmujący głównie Azję Zachodnią , ale w pewnych kontekstach także obszary Europy i Afryki .
Geografia .
Obejmuje on głównie państwa położone w południowo-zachodniej Azji .
Najczęściej do krajów Bliskiego Wschodu zalicza się : Arabię Saudyjską , Bahrajn , Egipt , Irak , Iran , Izrael , Palestynę , Jemen , Jordanię , Katar , Kuwejt , Liban , Oman , Syrię , Turcję , Zjednoczone Emiraty Arabskie oraz w niektórych źródłach też Cypr .
W sumie zajmują one powierzchnię ok. 9,7 mln km² z liczbą ludności wynoszącą 456 mln mieszkańców ( 2021 ) .
Historia .
Bliski Wschód jest regionem , gdzie prawdopodobnie rozpoczęły się procesy przejścia z paleolitu do neolitu .
Region ten ( Żyzny Półksiężyc ) jest również kolebką wielkich cywilizacji starożytnych ( Mezopotamia , starożytny Egipt ) oraz trzech wielkich religii abrahamowych : judaizmu , chrześcijaństwa i islamu .
Przez prawie trzydzieści wieków było to centrum starożytnego świata .
