Blois
Blois ( wym .
[ ] ) – miejscowość i gmina we Francji , w Regionie Centralnym , w departamencie Loir-et-Cher nad rzeką Loarą .
Według danych na rok 1990 gminę zamieszkiwało 49 318 osób , a gęstość zaludnienia wynosiła 1317 osób/km² ( wśród 1842 gmin Centre Blois plasuje się na 5. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 203 .
) .
Miasto królewskie .
Blois było przez wiele wieków siedzibą królewską a tym samym Zamek w Blois górujący nad miastem był świadkiem wielu historycznych wydarzeń , także narodzin wielu potomków królewskich i śmierci członków rodzin królewskich .
Jego budowa rozpoczęła się w XIII wieku .
