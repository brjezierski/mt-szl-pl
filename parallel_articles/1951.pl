Blumeriella jaapii
Blumeriella jaapii ( Rehm ) Arx – gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Pasożyt , wywołujący drobną plamistość liści drzew pestkowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Blumeriella , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1907 r. Heinrich Rehm nadając mu nazwę `` Pseudopeziza jaapii '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1961 r. Josef Adolph von Arx .
