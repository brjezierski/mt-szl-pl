Kalafior ( skała )
Kalafior – skała w Parku Jurajskim w województwie śląskim , w powiecie zawierciańskim , w gminie Pilica .
Pod względem geograficznym znajduje się na Wyżynie Ryczowskiej , będącej częścią Wyżyny Częstochowskiej w obrębie Wyżyny Krakowsko-Częstochowskiej .
Szczurek znajduje się w lesie po lewej stronie drogi ze Smolenia do Złożeńca .
Są tutaj 4 skały , na których uprawiana jest wspinaczka skalna : Kalafior , Szczurek , Oczko i Salceson .
Kalafior znajduje się na wzniesieniu i jest najbardziej wśród nich wysunięty na zachód .
Jest to zbudowana ze skalistego wapienia skała o wysokości do 12 m. Do maja 2020 roku wspinacze poprowadzili na niej 9 dróg wspinaczkowych o trudności od VI do VI.4+ w skali polskiej .
8 z nich ma zamontowane stałe punkty asekuracyjne : ringi ( r ) i ringi zjazdowe ( rz ) .
