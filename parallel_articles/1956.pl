Bob Dylan ( album )
Bob Dylan – debiutancki album Boba Dylana , wydany w 1962 roku .
Zawiera głównie tradycyjne ballady i utwory bluesowe , i tylko 2 kompozycje Dylana ( „ Talkin ' New York ” i „ Song to Woody ” , będąca hołdem dla Woody ’ ego Guthrie ) .
Album został wydany przez Columbia Records w formie płyty gramofonowej .
Producentem był John Hammond .
