Bobowa ( stacja kolejowa )
Bobowa – stacja kolejowa w Bobowej , w województwie małopolskim , w powiecie gorlickim , w Polsce .
W roku 2017 stacja obsługiwała 20–49 pasażerów na dobę .
Przy wyjeździe ze stacji w stronę przystanku Bobowa Miasto ( w stronę Nowego Sącza ) , znajduje się tunel oddany do użytku w 2006 na miejscu starego wiaduktu .
Przebiega on pod drogą wojewódzką nr 981 .
