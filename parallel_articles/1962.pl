Bobowo
Bobowo – wieś kociewska w Polsce położona w województwie pomorskim , w powiecie starogardzkim , siedziba gminy Bobowo .
Leży przy drodze wojewódzkiej nr 222 i na trasie ( zawieszonej obecnie ) linii kolejowej nr 243 Starogard Gdański-Skórcz .
Wieś królewska w 1664 roku należała do starostwa gniewskiego .
W czasie istnienia gromad miejscowość była siedzibą gromady Bobowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Przez miejscowość przepływa Węgiermuca , niewielka rzeka dorzecza Wisły , prawy dopływ Wierzycy .
