Bochotnica ( Nałęczów )
Bochotnica , Bochotnica Kościelna – część miasta Nałęczów .
Pierwotnie osada z okresu IX i X wieku , a następnie w XIV wieku wieś parafialna .
Została włączona do Nałęczowa w 1962 .
Leży w południowej części miasta , nad rzekami Bochotniczanką i Bystrą .
Obejmuje ulice : Bochotnica , Cynkowską , Graniczną , Łąkową , Słowiczą , Wojciechowską i Grzegorza Wójcika .
Wieś królewska położona była w drugiej połowie XVI wieku w powiecie lubelskim w ziemi lubelskiej województwa lubelskiego .
Miejscowość stanowi sołectwo gminy Nałęczów .
Historia .
W dokumentach występuje od 1350 roku .
Notowana jest pod następującymi nazwami : w roku 1350 była to „ Bochotnicza ” , następnie 1470–80 „ Bochotnycza Maior ” – „ Bochotnica Duża ” , 1529 „ Bochothnycza episcopalis ” – „ Bochotnica Biskupia ” , od końca XVIII wieku „ Bochotnica Kościelna ” .
Wieś stanowiła własność królewską w tenucie Kazimierz , od 1523 własność szlachecka dziedziczna .
