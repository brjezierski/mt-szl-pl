Bochum
Bochum – miasto na prawach powiatu położone w zachodniej części Niemiec , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Arnsberg , w środku Zagłębia Ruhry ( niem .
`` Ruhrgebiet '' ) .
Duży ośrodek przemysłowy : szóste co do wielkości miasto kraju związkowego , znajduje się także w pierwszej dwudziestce największych miast Niemiec .
W Bochum znajduje się sześć szkół wyższych , w tym jedna z większych w kraju – Ruhr-Universität z 30 000 studentów .
Posiada umowę partnerską o wymianie i współpracy z wieloma uczelniami , w tym Uniwersytetem Wrocławskim .
Górnictwo nie stanowi dziś już ważnej roli w życiu miasta – Bochum stało się centrum usług .
Mimo to znajdują się tu jeszcze instytucje związane z górnictwem .
Do historii przemysłu przeszedł Związek Bochumski , któremu udało się w 1842 r. odlać stal w formach .
