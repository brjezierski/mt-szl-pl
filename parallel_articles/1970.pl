Bochówko
Bochówko ( dodatkowa nazwa w j. kaszub .
Bòchówkò ; niem .
`` Bochowke '' ) – osada kaszubska w Polsce , położona w województwie pomorskim , w powiecie bytowskim , w gminie Czarna Dąbrówka , na północno-zachodnim krańcu Pojezierza Kaszubskiego .
Wchodzi w skład sołectwa Bochowo .
W latach 1975–1998 osada administracyjnie należała do województwa słupskiego .
Historia .
Do roku 1945 wieś znajdowała się w granicach Niemiec .
29 grudnia 1937 roku historycznie ukształtowana , niemiecka nazwa miejscowości `` Bochowke `` została przez administrację nazistowską zastąpiona w ramach szerokiej akcji deslawizacji nazw miejscowych formą `` Hohenlinde '' .
