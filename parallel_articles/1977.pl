Bodø ( stacja kolejowa )
Bodø – stacja kolejowa w Bodø w Norwegii .
Jest najbardziej na północ wysuniętą stacją połączoną z resztą kraju koleją przebiegającą wyłącznie po terytorium Norwegii .
Jest dużą stacją towarową .
Historia .
Budowę linii do Bodø rozpoczęto już w 1923 roku , jednak z powodu braku funduszy i II wojny światowej stację otwarto 1 stycznia 1961 roku , z początku jako towarową obsługującą pobliski port .
Oficjalne otwarcie z udziałem króla Olafa odbyło się 7 czerwca 1962 .
Obsługa pasażerów .
Automaty biletowe , parking na 50 miejsc , kawiarnia , przystosowanie do obsługi niepełnosprawnych .
Stacja jest nieczynna w weekendy .
