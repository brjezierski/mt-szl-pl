Boeremia exigua ( Desm . )
Aveskamp , Gruyter & amp ; Verkley – gatunek grzybów z klasy `` Dothideomycetes '' .
Pasożyt wielu roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Boeremia , Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1849 r. Jean Baptiste Henri Joseph Desmazières , nadając mu nazwę `` Phoma exigua '' .
Obecną , uznaną przez Index Fungorum nazwę , nadali mu Maikel M. Aveskamp , Johannes de Gruyter i Gerard J.M .
Verkley w 2010 r .
Jest gatunkiem typowym rodzaju `` Boeremia '' .
Ma ponad 20 synonimów .
Niektóre z nich :
Charakterystyka .
Jest to gatunek powszechnie występujący .
W. Mułenko i inni w 2003 r. podali jego występowanie ( jako `` Phoma exigua '' ) na bardzo licznych roślinach , zarówno dziko żyjących , jak i na roślinach uprawnych .
