Bogatynia
Bogatynia ( dawniej `` Rychwałd '' , , ) – miasto w południowo-zachodniej Polsce , w województwie dolnośląskim , w powiecie zgorzeleckim , siedziba władz gminy miejsko-wiejskiej Bogatynia .
Położone na Obniżeniu Żytawsko-Zgorzeleckim , nad potokiem Miedzianka , w polskiej części Łużyc Górnych .
Według danych GUS z 31 grudnia 2019 r. miasto miało 17 318 mieszkańców .
Jest to jedna z najbogatszych gmin w Polsce , na jej terenie znajdują się Kopalnia Węgla Brunatnego Turów oraz Elektrownia Turów zatrudniające łącznie ok. 5,5 tysiąca osób .
Położenie .
Według danych z 1 stycznia 2009 powierzchnia miasta wynosi 59,88 km² .
Bogatynia leży w Kotlinie Turoszowskiej , która ograniczona jest od południa Granicznym Wierchem ( 616 m n.p.m. , Góry Izerskie ) , a od północy Wyniosłością Działoszyńską ( ok. 340 m n.p.m . ) .
Najniższym punktem , a zarazem sztucznie utworzonym jest kopalnia odkrywkowa PGE KWB Turów , o wys .
20 m p.p.m .
Miasto położone jest nad potokiem Miedzianka .
W Bogatyni do Miedzianki uchodzą potoki Jaśnica oraz mniejszy Czerwienica .
