Boguchwałowice
Boguchwałowice ( ros .
Богухваловицы , nazwa wsi obowiązującą w czasach zaboru rosyjskiego ) – wieś w Polsce położona w województwie śląskim , w powiecie będzińskim , w gminie Mierzęcice , nad zbiornikiem Przeczyce ( nazywanym czasami Zalewem Przeczycko-Siewierskim ) - mimo że duża większość obszaru zalewu mieści się właśnie tutaj .
Oddalona od Katowic o około 35 km .
Wieś biskupstwa krakowskiego w księstwie siewierskim w końcu XVI wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa katowickiego .
Do Boguchwałowic dojeżdżają autobusy MZKP co umożliwia mieszkańcom bezpośredni dojazd do Będzina i Tarnowskich Gór .
We wsi znajduje się kilka sklepów , kompleks barów , pole namiotowe i gospodarstwa agroturystyczne .
Latem ludność Boguchwałowic zwiększa się kilkakrotnie , dzięki turystom przybywającym na plażę Zalewu Przeczycko-Siewierskiego .
Od roku 2014 wieś posiada kanalizację .
Znajduje się tu również tama ( nazywana przez mieszkańców Małą Tamą , w odróżnieniu od Dużej Tamy w sąsiednich Przeczycach ) z upustem wody z przepływającego przez wieś strumyka .
Obok tamy znajduje się także źródło wody pitnej i stadnina .
Od 1926 r. działa w Boguchwałowicach Ochotnicza Straż Pożarna .
