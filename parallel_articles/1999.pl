Bohoniki
Bohoniki – wieś w Polsce położona w województwie podlaskim , w powiecie sokólskim , w gminie Sokółka zamieszkiwana przez mniejszość tatarską .
We wsi znajduje się meczet oraz mizar – cmentarz muzułmański .
Bohoniki leżą na historycznej Grodzieńszczyźnie , w powiecie grodzieńskim , w województwie trockim , na obszarze ekonomii grodzieńskiej .
W latach 1975–1998 miejscowość położona była w województwie białostockim .
Wierni kościoła rzymskokatolickiego należą do parafii Narodzenia Najświętszej Maryi Panny w Kundzinie .
Historia .
Rotmistrz Olejowski otrzymał Bohoniki od króla Sobieskiego jako rekompensatę za niewypłacony żołd .
Istnieje także bardziej romantyczna wersja , każąca wierzyć , że król obdarował włościami Tatara , który za wierna służbę u jego boku otrzymać miał tyle ziemi , ile zdąży objechać konno w ciągu całego dnia .
W rzeczywistości jednak w 1679 osiedliło się pod Sokółką 30 rodzin lipkowskich , podkomendnych rotmistrzów : Kieńskiego , Olejowskiego i Sieleckiego .
Obok Drahli , Malawicz i Kamionki swoje ziemie dostali muzułmanie także w Bohonikach .
To właśnie tu postanowili wybudować meczet , którego istnienie odnotowano już w 1717 .
W 1789 w Bohonikach znajdowały się 3 folwarki i 5 gospodarstw drobnoszlacheckich ; zamieszkiwało je 46 Tatarów .
W 1921 Bohoniki zamieszkiwało 26 Tatarów .
Obecnie w tej małej wiosce , liczącej zaledwie 30 domów mieszkają cztery rodziny muzułmanów , w sumie 14 osób .
Meczet , którym się opiekują , niestety nie jest tym samym , który pamięta jeszcze czasy króla Jana .
Według lokalnej tradycji meczet znajdował się w okolicy starego mizaru we wschodniej części wsi i uległ pożarowi .
Mimo to odbudowana w XIX wieku świątynia figuruje w rejestrze zabytków i ściąga do Bohonik nie tylko pielgrzymów , ale i turystów .
