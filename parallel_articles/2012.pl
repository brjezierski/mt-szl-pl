Bolesław Abart
Bolesław Abart ( ur .
13 sierpnia 1935 w Chorzowie ) – polski aktor teatralny , filmowy i telewizyjny .
Życiorys .
Ukończył krakowską PWST w 1962 .
W tym samym roku , 11 czerwca , zadebiutował w teatrze i został zaangażowany do krakowskiego Teatru im .
J. Słowackiego .
Występował w nim do 1964 , po czym przeniósł się do wrocławskiego Teatru Polskiego , by od 1974 zostać aktorem wrocławskiego Teatru Współczesnego .
Gra tam do chwili obecnej .
