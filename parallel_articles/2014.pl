Bolesław ( imię )
Bolesław ( `` bole ( j ) '' + `` sław '' – „ bardziej sławny ” , por .
Więcesław , Wacław ) – słowiańskie imię męskie .
Zdrobniale Bolko .
Dawniej zapisywane również w formie skróconej Bosław .
Imię dynastyczne w polskiej dynastii Piastów , oraz czeskiej Przemyślidów .
Do dynastii piastowskiej zostało zapożyczone od Przemyślidów poprzez dziedziczenie po linii żeńskiej ( imię kognacyjne ) .
Ostatnim jego piastowskim przedstawicielem był Piast mazowiecki , Bolesław V , książę warszawski ( XV w. ) .
Bolesław imieniny obchodzi 23 lipca , 12 czerwca i 19 sierpnia .
Podobne imiona : Bolebor , Boleczaj , Boleczest , Bolelut , Bolemir oraz Bolemysł .
