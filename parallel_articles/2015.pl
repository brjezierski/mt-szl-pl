Borowik ciemnobrązowy
Borowik ciemnobrązowy ( `` Boletus aereus '' Bull . )
– gatunek grzybów z rodziny borowikowatych ( `` Boletaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Boletus , Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Niektóre synonimy nazwy naukowej :
Nazwę polską podał M. Wilga w 1996 r. W niektórych ujęciach systematycznych dawniej uważany był za podgatunek lub odmianę borowika szlachetnego ( jako borowik szlachetny podgatunek spiżowy lub borowik szlachetny odmiana ciemnobrązowa ) .
Według Index Fungorum jest to odrębny gatunek .
Morfologia .
Jest gruby i czasami pomarszczony , a jego średnica dochodzi do 30 cm .
U młodych egzemplarzy jest półkolisty , później płaskołukowaty , na koniec płaski .
Powierzchnia sucha , początkowo zamszowa , później gładka i naga , czasami popękana .
