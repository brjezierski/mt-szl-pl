Gęstoporek cynobrowy
Gęstoporek cynobrowy ( `` Pycnoporus cinnabarinus '' ( Jacq . )
P. Karst ) – gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pycnoporus , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Niektóre synonimy naukowe :
Po raz pierwszy takson ten zdiagnozował w 1776 r. Jacquin nadając mu nazwę `` Boletus cinnabarinus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1941 r. P. Karsten , przenosząc go do rodzaju `` Pycnoporus '' .
Nazwa polska pojawiła się w pracy Stanisława Domańskiego i in .
w 1967 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako huba cynobrowa , wrośniak cynobrowy , żagiew cynobrowa .
Morfologia .
O szerokości do 10 cm , półkolisty lub wachlarzowaty i bokiem przyrośnięty do podłoża .
Brzeg cienki i ostry .
Górna powierzchnia zwykle nierówna , pomarszczona .
Kolor cynobrowy , czerwonawy lub ceglasty , u starszych osobników płowieje .
W postaci rurek o porach w kolorze cynobrowym lub czerwonawym .
Pory okrągłe lub kanciaste .
O grubości do 2 cm , cynobrowego koloru , elastyczny , włóknisty i bez zapachu .
Wysyp zarodników biały .
Zarodniki nieamyloidalne , cylindryczne lub eliptyczne , gładkie i bezbarwne o rozmiarach 5–8 × 2,5–3 μm .
Cystyd brak .
