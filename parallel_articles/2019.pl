Boliwia
Boliwia ( ) , oficjalnie Wielonarodowe Państwo Boliwia ( ) – państwo śródlądowe w środkowo-zachodniej części Ameryki Południowej , graniczy z Brazylią na północy i wschodzie , Paragwajem i Argentyną na południu oraz z Chile i Peru na zachodzie .
Warunki naturalne .
Boliwia jest jednym z dwóch krajów Ameryki Południowej , które nie mają dostępu do morza ( straciła go w 1884 ) .
Na zachodzie wznoszą się góry , natomiast na wschodzie teren się obniża .
Pasmo Andów o szerokości ok. 600 km , składa się tu z trzech głównych łańcuchów – Kordyliery Zachodniej ( najwyższy szczyt Boliwii : Sajama – 6520 m ) , Kordyliery Środkowej oraz Kordyliery Wschodniej .
Pomiędzy Kordylierami Zachodnią i Środkową rozciąga się płaskowyż Altiplano ( `` hiszp .
altiplano – płaskowyż '' ) .
Na wschód teren opada przechodząc w przedgórze Yungas i w końcu w Nizinę Boliwijską .
Na południowym wschodzie kraju leży część Gran Chaco , a na północy rozciąga się obszar Niziny Amazonki .
Największe rzeki Boliwii to Mamoré , Beni i Guaporé .
Na Altiplano znajdują się bezodpływowe jeziora ( Titicaca , Poopó ) oraz solniska ( Salar de Uyuni ) .
Klimat .
Na nizinach Boliwii panuje klimat podrównikowy z roczną sumą opadów wahającą się w granicach od 2000 do 3000 mm ; na przedgórzu klimat kontynentalny wilgotny ( opady 1500–2000 mm ) , a w Andach kontynentalny suchy z sumą opadów do 600 mm .
Flora .
Na Nizinie Boliwijskiej znajdują się przede wszystkim lasy równikowe ( lasy stanowią 51 % powierzchni kraju ) i sawanny .
W górach dominują formacje półpustynne : puna i jalca .
W Boliwii istnieje 13 parków narodowych .
Najbardziej znany to Sajama .
Fauna .
W lasach równikowych Boliwii można spotkać m.in .
jaguary , pumy , kapibary , kajmany , tapiry i małpy ( zwierzęta , które preferują klimat wilgotny ) .
Historia .
Tereny obecnej Boliwii zamieszkane były przez kultury prekolumbijskie od około 20 000 lat .
Pierwszą z wysoce rozwiniętych była kultura Tiahuanaco , która rozwinęła się na południowym wybrzeżu jeziora Titicaca około II wieku p.n.e .
W XV wieku terytorium ludu Ajmara zostało podbite i włączone w obręb Państwa Inków .
W XVI wieku hiszpańscy konkwistadorzy pod wodzą Francisco Pizarro dokonali podboju terenów obecnej Boliwii .
Jako Górne Peru zostały one włączone do nowo powstałego Wicekrólestwa Peru , a następnie do Wicekrólestwa La Platy .
Nazwa państwa – nadana mu po oderwaniu się w roku 1825 od Peru – pochodzi od nazwiska przywódcy tegoż ruchu Bolivara .
Po zrzeczeniu się władzy przez Antonio José de Sucre w roku 1827 , na stanowisko prezydenta wybrano Andrésa Santa Cruz , który uchylił uchwaloną za Simona Bolivara konstytucję .
W roku 1836 Santa Cruz zajął Peru i utworzył Konfederację Peruwiańsko-Boliwijską .
Konfederacja przetrwała do roku 1839 , kiedy na skutek przegranej wojny z Chile i Argentyną Peru odzyskało niepodległość .
16 czerwca 1839 r. kongres ustanowił generała Velasco tymczasowym prezydentem Republiki do czasu nowych wyborów .
