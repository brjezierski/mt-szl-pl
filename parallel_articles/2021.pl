Bolonia
Bolonia ( wł .
`` Bologna '' ) – miasto w północnych Włoszech , położone jest między Padem a Apeninami na wysokości 54 m n.p.m .
Miasto leży nad dwiema rzekami – Reno i Saveną .
Stolica regionu Emilia-Romania .
Dane ogólne .
Ośrodek administracyjny prowincji Bolonia , na Nizinie Padańskiej , pomiędzy rzeką Pad a Apeninami .
Ważny ośrodek handlowy , przemysłowy , kulturowy , naukowy ( najstarszy na świecie uniwersytet założony w 1088 ) i turystyczny .
Bolonia nazywana jest też `` La Grassa '' ( tłusta ) ze względu na swą tradycyjnie kaloryczną kuchnię .
