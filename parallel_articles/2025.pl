Bondary ( rejon brasławski )
Bondary ( ; ) – chutor na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Druja .
Historia .
W czasach zaborów wieś leżała w granicach Imperium Rosyjskiego .
W latach 1921–1945 wieś leżała w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie brasławskim , w gminie Słobódka .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 49 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Było tu 9 budynków mieszkalnych .
W 1931 w 10 domach zamieszkiwało 47 osób .
Miejscowość należała do parafii rzymskokatolickiej w Słobódce .
Podlegała pod Sąd Grodzki w Brasławiu i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Słobódce .
