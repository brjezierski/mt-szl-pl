Jodłownica górska
Jodłownica górska , bondarcewia górska ( `` Bondarzewia mesenterica '' ( Schaeff . )
Kreisel ) – gatunek grzybów należący do rodziny jodłownicowatych ( `` Bondarzewiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Bondarzewia , Bondarzewiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1774 r. Jacob Christian Schaeffer nadając mu nazwę `` Boletus mesentericus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1984 r. Hanns Kreisel przenosząc go do rodzaju `` Bondarzewia '' .
Synonimy naukowe :
Nazwę polską ( jodłownica górska ) podali Barbara Gumińska i Władysław Wojewoda w 1985 r. , przez S. Domańskiego gatunek ten opisywany był w 1967 r. jako bondarzewia górska .
Morfologia .
Jednoroczne , duże .
Tworzą zwykle rozetkowe skupienia pojedynczych , zrośniętych dachówkowato kapeluszy , wyrastających ze wspólnego trzonu .
