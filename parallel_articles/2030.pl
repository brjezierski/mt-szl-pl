Bonifacio
Bonifacio ( kors .
`` Bunifaziu '' ) – miejscowość i gmina we Francji , w regionie Korsyka , w departamencie Korsyka Południowa .
Według danych na rok 1990 gminę zamieszkiwały 2683 osoby , a gęstość zaludnienia wynosiła 19 osób/km² .
Bonifacio jest atrakcyjnie położonym nad Cieśniną Świętego Bonifacego miasteczkiem , składającym się z dwóch części `` Dolnego Bonifacio '' i `` Górnego Bonifacio '' ( położonego na skalistym południowym brzegu Korsyki ) .
Roztacza się stąd ( przy dobrej pogodzie ) panoramiczny widok na pobliską Sardynię .
W `` Górnym Bonifacio '' znajduje się mała forteca obronna i cmentarz z czasów napoleońskich .
Znajduje się tu kilka tawern serwujących lokalne korsykańskie specjalności ( w szczycie sezonu lipiec/sierpień zaleca się uprzednią rezerwację ) .
