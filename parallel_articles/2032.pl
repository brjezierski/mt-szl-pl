Bonn
Bonn ( `` Bundesstadt Bonn '' – miasto federalne Bonn ) – miasto na prawach powiatu w zachodnich Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , po obu stronach rzeki Ren .
Bonn leży ok. 30 km na południe od Kolonii .
W połowie drogi do Kolonii leży port lotniczy Köln/Bonn .
Historia miasta liczy ponad 2000 lat i sięga do czasów germańskich i rzymskich osad .
Od 1597 do 1794 Bonn było rezydencją książąt kolońskich , w 1770 roku przyszedł tu na świat Ludwig van Beethoven , a w 1856 pochowano tu Roberta Schumanna .
W latach 1949–1991 miasto było stolicą RFN ; po zjednoczeniu dwóch państw niemieckich stolicą ponownie stał się Berlin .
Historia .
Miasto powstało jako fort założony przez legionistów rzymskich , wówczas nazywane było Bonna .
W 1770 roku w Bonn urodził się Ludwig van Beethoven .
W 1815 roku kontrolę nad miastem przejęły Prusy .
Po II wojnie światowej miasto było stolicą RFN , aż do przeniesienia stolicy do Berlina w latach 90 .
