Bonnyrigg
Bonnyrigg – miasto we wschodniej Szkocji , w hrabstwie Midlothian , położone pomiędzy rzekami North Esk na zachodzie i South Esk na wschodzie , około 10 km na południowy wschód od Edynburga .
W 2011 roku miasto liczyło 15 677 mieszkańców .
W 1750 roku odnotowana została w tym miejscu niewielka osada pod nazwą Bonnebrig .
Miejscowość rozwinęła się od końca XVIII wieku jako wieś górnicza , ośrodek wydobycia węgla .
W 1855 roku otwarto tu stację kolejową na linii z Edynburga do Peebles ( zamknięta w 1965 ) .
