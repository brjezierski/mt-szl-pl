Borcz
Borcz ( dodatkowa nazwa w j . )
– wieś kaszubska w Polsce położona w województwie pomorskim , w powiecie kartuskim , w gminie Somonino , przy drodze krajowej nr 20 Stargard - Szczecinek - Gdynia , na turystycznym szlaku Wzgórz Szymbarskich .
W latach 1975–1998 wieś administracyjnie należała do województwa gdańskiego .
Siedziba sołectwa Borcz , w skład którego wchodzą również : Borcz-Leśniczówka , Mały Dwór i Patoka .
W Borczu znajduje się dwór ( obecnie przekształcony na hotel ) z parkiem , a na północ od miejscowości położony jest rezerwat przyrody Jar Rzeki Raduni .
W czasie II wojny zarządcą majątku był pochodzący z rodziny gdańskich patrycjuszy z ul .
Piwnej ( Höhne Haus ) Albert Höhne , wstawiający się u władz niemieckich za aresztowanymi i wytypowanymi do deportacji Polakami oraz otaczający ochroną miejsca kultu religijnego .
