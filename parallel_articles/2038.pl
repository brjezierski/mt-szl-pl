Bordeaux
Bordeaux ( wym .
MAF ; oksyt .
`` Bordèu '' ) – miasto i gmina we Francji , stolica regionu Nowa Akwitania , w departamencie Żyronda .
Dane ogólne .
Według danych na rok 2018 gminę zamieszkiwało 260 352 osób , a gęstość zaludnienia wynosiła 5274 osób/km² ( wśród 2290 gmin Akwitanii Bordeaux plasuje się na 1. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na 122. miejscu ) .
Według danych z 2005 Bordeaux zamieszkiwało 230 600 osób .
Bordeaux jest miastem w południowo-zachodniej Francji .
Miasto położone jest nad rzeką Garonną , 97 km od Oceanu Atlantyckiego .
Okolice miasta są znane na całym świecie przede wszystkim z produkcji win , wytwarzanych tam od VIII wieku .
Działa tam polski konsulat honorowy .
