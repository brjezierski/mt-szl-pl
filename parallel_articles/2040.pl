Borger ( Teksas )
Borger – miasto w Stanach Zjednoczonych , w stanie Teksas , w hrabstwie Hutchinson .
Po odkryciu w okolicy złóż ropy naftowej zostało wytyczone miejsce na nowe miasto .
W ciągu dziewięćdziesięciu dni ściągnęło do niego 45 tysięcy osób , toteż już październiku tego samego roku Borger uzyskało prawa miejskie .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 roku miasto liczyło 13 251 mieszkańców .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 80,6 % , Afroamerykanie 3,6 % , Azjaci 0,5 % , pozostali 15,3 % .
