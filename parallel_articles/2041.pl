Borgholm
Borgholm – miejscowość ( tätort ) w południowo-wschodniej Szwecji , na wyspie Olandia w regionie Kalmar , jest siedzibą gminy Borgholm .
Miejscowość zamieszkuje około 3093 mieszkańców .
Prawa miejskie posiada od 1816 .
W mieście rozwinął się przemysł rybny .
W pobliżu miasta znajdują się ruiny zamku zburzonego w 1806 .
