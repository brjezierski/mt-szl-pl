Borgo Maggiore
Borgo Maggiore – jeden z 9 zamków San Marino .
Jest drugim co wielkości zamkiem w państwie .
Dawniej nazywany był `` Mercatale '' ( Rynek ) .
Do dziś pozostaje jedną z najważniejszych miejscowości handlowych .
W 2012 roku liczył 6609 mieszkańców .
Geografia .
Borgo Maggiore graniczy z sanmaryńskimi miastami Serravalle , Domagnano , Faetano , Fiorentino , San Marino i Acquaviva oraz włoskim Verucchio .
