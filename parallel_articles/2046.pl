Borgowo
Borgowo ( niem .
`` Burgdorf '' ) – wieś w Polsce położona w województwie wielkopolskim , w powiecie śremskim , w gminie Śrem .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
Położona przy drodze wojewódzkiej nr 434 z Łubowa przez Śrem do Rawicza przez Gostyń .
We wsi znajduje się skrzyżowanie z drogą do Pyszącej .
Komunikację ze wsi do Śremu zapewnia komunikacja gminna oraz autobusy PKS w Poznaniu .
Zabytkami wsi znajdującymi się w gminnej ewidencji zabytków jest figura Matki Boskiej Królowej Polski z 1918 oraz cmentarz ewangelicki założony w XIX wieku .
