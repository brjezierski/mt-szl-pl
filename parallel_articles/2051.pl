Borki-Kosiorki
Borki-Kosiorki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Jezusowego w Wiśniewie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
We wsi znajduje się przystanek kolejowy Kosiorki na linii kolejowej E 20 Moskwa – Warszawa – Berlin .
Przez wieś przebiega droga powiatowa Mościbrody – Borki-Paduchy .
W miejscowości działa założona w 1969 roku jednostka ochotniczej straży pożarnej .
Jest to jednostka typu M , czyli nie posiadająca samochodu , a jedynie motopompę wożoną na pojeździe zastępczym .
