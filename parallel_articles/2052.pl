Borki-Paduchy
Borki-Paduchy – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Leży nad rzeką Zbuczynką .
Przez wieś przebiega droga powiatowa Mościbrody – Zbuczyn Poduchowny i droga do Wiśniewa .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Stanisława i Aniołów Stróżów w Zbuczynie .
