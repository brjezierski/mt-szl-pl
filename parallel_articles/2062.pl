Borowica ( rzeka )
Borowica ( ) – rzeka w południowej Bułgarii , w obwodzie Smolan i obwodzie Kyrdżali .
Rzeka wypływa pod nazwą Chambardere na wysokości na wysokości 1622 m n.p.m. , z grzbietu Pereliksko-Presnanskija , u podnóża szczytu górskiego Ełwarinka ( 1715 m n.p.m . )
w Rodopach Zachodnich .
Wpada do lewego brzegu Ardy , na wysokości 331,5 m n.p.m .
Rzeka ma 42,1 km długości , średni przypływ wynosi 5 m³/s oraz powierzchnię dorzecza o wielkości 301 km² , co stanowi 5,19 % powierzchni dorzecza Ardy .
