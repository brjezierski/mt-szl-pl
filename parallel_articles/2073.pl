Boruty ( gmina Sypniewo )
Boruty – wieś w Polsce położona w województwie mazowieckim , w powiecie makowskim , w gminie Sypniewo .
W latach 1975–1998 miejscowość należała administracyjnie do województwa ostrołęckiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Małgorzaty w Gąsewie Poduchownym .
