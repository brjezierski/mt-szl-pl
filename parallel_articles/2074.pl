Borucin ( przystanek kolejowy )
Borucin – nieczynny wąskotorowy przystanek osobowy w Borucinie , w gminie Osięciny , w powiecie radziejowskim , w województwie kujawsko-pomorskim , w Polsce .
Został wybudowany w 1914 roku przez okupacyjne wojska niemieckie razem z linią kolejową z Płowiec do Strykowa .
