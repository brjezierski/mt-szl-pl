Boryszew ( przedsiębiorstwo )
Boryszew SA – spółka akcyjna notowana na Giełdzie Papierów Wartościowych w Warszawie .
Spółka jest notowana w ramach indeksu WIG30 w Warszawie .
Przedsiębiorstwa wchodzące w skład grupy kapitałowej Boryszew zajmują się produkcją komponentów dla sektora motoryzacyjnego , materiałów chemicznych ( m.in .
płyn chłodniczy Borygo , płyn odkażający ERG CleanSkin ) , tlenków metali oraz elementów metalowych .
Zatrudnia niemal 12 tys .
