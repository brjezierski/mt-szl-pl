Borysław ( stacja kolejowa )
Borysław ( , ) – stacja kolejowa w miejscowości Borysław , w rejonie drohobyckim , w obwodzie lwowskim , na Ukrainie .
Jest to stacja krańcowa linii .
Stacja powstała w czasach Austro-Węgier .
Obecnie na linii Drohobycz – Borysław nie są prowadzone przewozy pasażerskie .
