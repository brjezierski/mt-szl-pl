Bosanski Petrovac
Bosanski Petrovac – miasto w Bośni i Hercegowinie , w Federacji Bośni i Hercegowiny , w kantonie uńsko-sańskim , siedziba gminy Bosanski Petrovac .
W 2013 roku liczyła 3427 mieszkańców , z czego większość stanowili Boszniacy .
Siedziba eparchii bihacko-petrovackiej Serbskiego Kościoła Prawosławnego .
W mieście znajduje się katedralny sobór Świętych Piotra i Pawła .
