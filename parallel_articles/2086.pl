Boskovice ( stacja kolejowa )
Boskovice – stacja kolejowa w miejscowości Boskovice , w kraju południowomorawskim , w Czechach .
Znajduje się na wysokości 370 m n.p.m .
Jest zarządzany przez Správę železnic .
Na stacji znajdują się kasy biletowe , na których istnieje możliwość zakupu biletów na wszystkie pociągi w tym międzynarodowe oraz rezerwacji miejsc .
