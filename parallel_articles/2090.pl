Język botlichyjski
Język botlichyjski ( nazwa własna Буйхальи миЦIи ) – jeden z niewielkich języków kaukaskich , używany przez Botlichów .
Należy do języków andyjskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Dane , mówiące o liczbie osób posługujących się tym językiem są bardzo rozbieżne i niepewne , według statystyk w 1926 roku języka tego używało ok. 3,5 tys .
