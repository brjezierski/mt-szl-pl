Pajęczynowiec wąskozarodnikowy
Pajęczynowiec wąskozarodnikowy ( `` Botryobasidium intertextum '' ( Schwein . )
Jülich & amp ; Stalpers ) – gatunek grzybów z typu podstawczaków ( `` Basidiomycota '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botryobasidium , Botryobasidiaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1832 r. Lewis David von Schweinitz nadając mu nazwę `` Sporotrichum intertextum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Walter Jülich i Joos A. Stalpers w 1958 r .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
