Botryotinia squamosa
Botryotinia squamosa Vienn.-Bourg .
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botrytinia , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Jego anamorfa znana jest jako `` Botrytis squamosa '' .
Pasożyt i saprotrof występujący w tkankach porażonych roślin i na ich obumarłych resztkach .
Na powierzchni porażonych roślin tworzy szary nalor złożony z konidioforów i zarodników konidialnych .
Na porażonych i pozostających w glebie resztkach roślin wytwarza sklerocja .
Kiełkują one na wiosnę i wytwarzają bezpłciowo ogromną ilość konidiów dokonujących infekcji pierwotnej młodych roślin .
Na sklerocjach czasami wytwarzane są także apotecja , w których na drodze płciowej powstają askospory .
Nie odgrywają one większej roli w rozprzestrzenianiu choroby .
Sklerocja mogą przetrwać w glebie do 21 cm na głębokości poniżej 15 cm .
Znaczenie .
Gatunek rozprzestrzeniony w Europie , USA , Azji i Australii Wśród roślin uprawnych poraża cebulę , czosnek i pora .
Wraz z innymi patogenami wywołuje u nich chorobę o nazwie zgnilizna szyjki cebuli .
