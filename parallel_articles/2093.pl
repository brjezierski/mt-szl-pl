Botrytis aclada
Botrytis aclada Fresen .
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Gatunek rozprzestrzeniony na całym świecie .
Pasożyt i saprotrof , wśród roślin uprawnych porażający cebulę , czosnek i pora .
Wraz z innymi patogenami wywołuje u nich chorobę o nazwie zgnilizna szyjki cebuli .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botrytis , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Morfologia .
Grzyb mikroskopijny , znany wyłącznie w postaci bezpłciowej anamorfy .
Rozwija się w tkankach porażonych roślin i na ich obumarłych resztkach .
Wytwarza w nich grzybnię z pyknidiami , w których powstają jajowate zarodniki konidialne o rozmiarach 8,4–12,1 × 4,6–5,6 μm .
Konidiofory mają długość około 1 mm .
Na podłożu PDA nie tworzy sklerocjów .
Przez długi czas `` B. aclada '' uważany był za synonim `` Botrytis allii '' .
Przyczyną były trudności z rozróżnieniem tych gatunków na podstawie cech morfologicznych .
W 1950 r. jednak Owen i współpracownicy analizując budowę chromosomów wykazali , że są to odrębne gatunki .
Trzy z 7 gatunków wywołujących zgniliznę szyjki cebuli , a mianowicie `` Botrytis allii '' , `` Botrytis aclada '' i `` Ciborinia allii '' są morfologicznie trudne do odróżnienia .
Wyniki badań metodami biologii molekularnej pozwoliły wyjaśnić relacje między nimi .
Porównanie sekwencji DNA wykazało , że `` Botrytis allii '' jest mieszańcem `` B. aclada '' i `` C. allii '' , i posiada allele genów od każdego z nich .
`` B. aclada '' i `` C. allii '' mają po 16 chromosomów , podczas gdy `` B. alli '' ma ich 32 .
