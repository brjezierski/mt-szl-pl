Botrytis allii
Botrytis allii Munn – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Gatunek rozprzestrzeniony na całym świecie .
Pasożyt i saprotrof .
Wśród roślin uprawnych poraża cebulę , czosnek i pora , wraz z innymi patogenami wywołując u nich chorobę o nazwie zgnilizna szyjki cebuli .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botrytis , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Morfologia .
Grzyb mikroskopijny , znany wyłącznie w postaci bezpłciowej anamorfy .
Rozwija się w tkankach porażonych roślin i na ich obumarłych resztkach .
Jego teleomorfa jest nieznana , ale u innych gatunków z rodzaju `` Botrytis '' należy ona do rodzaju `` Botryotinia '' .
Może być hodowany na sztucznych podłożach .
