Gronowiec szary
Gronowiec szary ( `` Botrytis cinerea '' Pers . )
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Według ankiety przeprowadzonej wśród fitopatologów przez czasopismo `` Molecular Plant Pathology '' w 2012 r. gatunek ten znalazł się na 2 miejscu gatunków grzybów o największym znaczeniu w gospodarce człowieka .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botrytis , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Jest anamorfą bliżej nieokreślonego gatunku z rodzaju `` Botryotinia '' .
Rozwój .
Z zarodników kiełkujących na powierzchni rośliny rozwijają się strzępki rostkowe , które przez naturalne szczeliny w tkankach bądź przez uszkodzenia wnikają do wnętrza rośliny , pobierając z niej substancje pokarmowe .
Na powierzchni tworzą szary , pylasty nalot składający się z bardzo licznych konidioforów , na końcach których wytwarzane są bezpłciowo ogromne ilości zarodników konidialnych dokonujących dalszych infekcji .
Zarodniki przenoszone są przez prądy powietrza , krople deszczu i w różny inny sposób .
Wytwarzane są zarówno makrokonidia , jak i mikrokonidia .
Te pierwsze są jednokomórkowe , bezbarwne , o elipsoidalnym kształcie i rozmiarach 9–15 × 6,5–10 μm .
Oprócz konidiów na powierzchni wytwarzane są także ( dużo rzadziej ) formy przetrwalnikowe – skleroty o barwie od ciemnobrunatnej do czarnej .
Gdy wykiełkują tworzą się na nich owocniki typu apotecjum z charakterystycznym trzonkiem .
W apotecjach powstają zarodniki płciowe – askospory .
Są jednokomórkowe , bezbarwne , elipsoidalne i mają rozmiar 9–12 × 4–6 μm .
Znaczenie .
Gatunek rozprzestrzeniony na całym świecie .
Poza Antarktydą występuje na wszystkich kontynentach , a także na niektórych wyspach .
Saprotrof i pasożyt .
Rozwija się na resztkach roślin , korzeni , sadzonkach , nasionach , owocach , kwiatach , kwiatostanach .
Atakuje bardzo wiele gatunków roślin , wywołując chorobę zwaną szarą pleśnią .
Choroba ta rozwija się u różnego rodzaju uprawianych roślin : w sadach , winnicach , ogrodach , na polach uprawnych , u roślin doniczkowych , w szkółkach leśnych , a także w przechowalniach owoców i warzyw i wywołuje duże szkody gospodarcze .
Rozwija się także na roślinach dziko rosnących .
