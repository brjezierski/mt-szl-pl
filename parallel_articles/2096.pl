Botrytis fabae
Botrytis fabae Sardiña – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botrytis , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Charakterystyka .
Grzyb mikroskopijny , znany wyłącznie w postaci bezpłciowej anamorfy .
Pasożyt i saprotrof występujący w tkankach porażonych roślin i na ich obumarłych resztkach .
Wytwarza w nich grzybnię z pyknidiami , w których powstają zarodniki konidialne .
Mają one rozmiar 16–25 ( –29 ) × ( 11– ) 13–16 ( –20 ) μm .
Tworzy także przetrwalnikowe sklerocja o średnicy 1–1,7 mm średnicy , rzadko do 3 mm .
Gatunek rozprzestrzeniony na całym świecie .
Występuje na licznych gatunkach roślin z rodziny bobowatych .
Wśród uprawianych roślin wywołuje choroby o nazwie czekoladowa plamistość bobu i czekoladowa plamistość bobiku .
Wśród roślin uprawnych najczęściej poraża bobik i bób , ale występuje także na grochu , fasoli , soczewicy .
