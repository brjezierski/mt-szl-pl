Botswana
Botswana , Republika Botswany ( ang .
`` Republic of Botswana '' , tswana `` Lefatshe la Botswana '' ) – państwo w południowej Afryce , bez dostępu do morza .
Do 1966 Botswana była brytyjskim protektoratem pod nazwą Beczuana , dziś członek Unii Afrykańskiej .
Graniczy z RPA ( 1840 km ) , Namibią ( 1360 km ) , Zimbabwe ( 813 km ) i Zambią ( ok. 0,02 km ) .
Największe miasta : Gaborone , Francistown , Maun , Selebi-Pikwe .
Geografia .
Botswana , położona w południowej Afryce , nie ma dostępu do morza , co sprawia , że pod wieloma względami jest ona uzależniona gospodarczo od swoich sąsiadów .
Większą część kraju zajmuje półpustynna , bezodpływowa kotlina Kalahari .
Jej średnia wysokość wynosi około 800 m n.p.m .
Na północy znajduje się bagienny obszar śródlądowej delty Okawango .
Okresowo wody przedostają się dalej na wschód do niecki Makgadikgadi .
Najważniejszą rzeką Botswany jest położona na południowo-wschodniej granicy Limpopo , gdzie koncentruje się ludność Botswany , w tym stolica kraju Gaborone .
W Botswanie panuje klimat podzwrotnikowy suchy z porą gorącą od października do marca .
Opady są skąpe ( od 750 mm na północy do 125 mm na południu ) i nieprzewidywalne .
Większość terytorium kraju porasta ciernista sawanna , a około 1 % widne suche lasy ( w dolinie rzeki Chobe na północy ) .
Historia .
Tereny dzisiejszej Botswany pierwotnie były zamieszkiwane przez Buszmenów .
Od przełomu XVI i XVII wieku zaczął na nie napływać lud Tswana , posługujący się językiem tswana z rodziny bantu .
W drugiej połowie XIX wieku rozpoczęły się walki plemienne , a także walki z Burami z Transwalu .
Doprowadziło to do objęcia Botswany ( nazywanej wówczas Beczuana , ang .
`` Bechuanaland '' ) brytyjskim protektoratem w roku 1885 .
Beczuana miała być przyłączona do Kraju Przylądkowego , ale ostatecznie ten projekt upadł .
W latach 50 .
XX wieku w Beczuanie powstały ruchy narodowowyzwoleńcze , które doprowadziły w roku 1966 do niepodległości kraju , pod nazwą Botswana .
Pierwszym prezydentem został Seretse Khama .
Wybrano go ponownie na dwie kolejne kadencje i zmarł pełniąc urząd 13 lipca 1980 .
