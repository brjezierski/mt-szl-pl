.de – domena internetowa przypisana do niemieckich stron internetowych .
Nazwa pochodzi od niemieckiej nazwy kraju , czyli Deutschland .
Początkowo za przydział adresów domeny .de odpowiadał Wydział Informatyki Uniwersytetu w Dortmundzie , stąd też adres internetowy tego uniwersytetu , uni-dortmund.de jest najstarszym zarejestrowanym adresem domeny .
Z kolei od stycznia 1994 do końca 1999 domeną zarządzał Instytut Informatyki Uniwersytetu w Karlsruhe .
