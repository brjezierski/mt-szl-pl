Bouaké
Bouaké – miasto w środkowej części Wybrzeża Kości Słoniowej , ośrodek departamentu Bouaké .
Drugie pod względem wielkości miasto kraju ( po Abidżanie ) .
W Bouaké znajduje się międzynarodowy Port lotniczy Bouaké .
Główną świątynią rzymskokatolickiej archidiecezji Bouaké jest Katedra św .
Teresy od Dzieciątka Jezus .
W 1992 roku założony został Université de Bouaké .
Znajduje się tu także Stade Bouaké , wielofunkcyjny stadion .
Miejsce urodzenia wielu piłkarzy iworyjskich , m.in .
Yaya Touré , Kolo Touré i Ibrahima Touré .
