Boulogne-Billancourt
Boulogne-Billancourt – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Według danych na rok 2015 gminę zamieszkiwały 119 127 osób , a gęstość zaludnienia wynosiła 19 307 osób/km² .
