Boulogne-sur-Mer
Boulogne-sur-Mer – miejscowość i gmina we Francji , w regionie Hauts-de-France , w departamencie Pas-de-Calais .
Według danych na rok 1990 gminę zamieszkiwało 43 678 osób , a gęstość zaludnienia wynosiła 5187 osób/km² .
Spośród 1549 gmin regionu Nord-Pas-de-Calais gmina Boulogne-sur-Mer plasuje się na 7. miejscu pod względem liczby ludności oraz na miejscu 422. pod względem powierzchni .
Od 7 do 12 sierpnia 1905 w Boulogne-sur-Mer ( esp .
`` Bulonjo-ĉe-Maro '' ) trwał pierwszy Światowy Kongres Esperanto , w którym wzięło udział 688 osób z 20 krajów świata .
