Bourges
Bourges ( wym .
MAF ) – miasto i gmina we Francji , w Regionie Centralnym , w departamencie Cher .
Miasto leży nad kanałem Berry , u zbiegu rzeki Auron i Yevre ( dorzecze Loary ) .
Stolica historycznej krainy Berry i ośrodek administracyjny departamentu Cher .
Według danych na rok 1990 gminę zamieszkiwało 75 609 osób , a gęstość zaludnienia wynosiła 1100 osób/km² ( wśród 1842 gmin Region Centralnego Bourges plasuje się na 3. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 19 . ) .
Miasto Bourges w 2008 r. liczyło 64 tys .
