Wawelno
Wawelno ( do 2002 r. `` Wąwelno '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie opolskim , w gminie Komprachcice .
Od 1950 r. miejscowość należy administracyjnie do województwa opolskiego .
Nazwa .
19 maja 1936 r. w miejsce nazwy Bowallno wprowadzono nazwę Walldorf .
12 listopada 1946 r. nadano miejscowości polską nazwę Wąwelno .
1 stycznia 2002 r. w miejsce nazwy Wąwelno wprowadzono nazwę Wawelno .
Nazwa miejscowości pochodzi od słowa „ wąwel ” , oznaczającego suche miejsce w bagiennym otoczeniu .
Historia .
Miejscowość po raz pierwszy wzmiankowano w księdze uposażenia biskupów wrocławskich ok. 1300 roku .
Później prawdopodobnie nastąpiła ponowna lokacja miejscowości – w 1326 r. wspominani są dwaj zasadźcy miejscowości Bowalno ( pojawiają się również formy Wadelno i Wamwalno ) – Stefan i Nikoska .
Niewiele wiadomo o dziejach miejscowości od średniowiecza do XIX wieku .
W połowie XIX stulecia działała szkoła z jednym nauczycielem , młyn wodny , a Wawelno zamieszkiwały 483 osoby , w tym pięciu rzemieślników .
W latach 60. funkcjonowała kopania węgla brunatnego , wydobywająca rocznie 5.000 ton tego surowca .
Wzrost liczby mieszkańców nastąpił na przełomie wieków – w 1890 r. było ich 729 , a w 1910 r. – 841 .
Najpewniej w pierwszej dekadzie XX w. rozpoczęła funkcjonowanie ochotnicza jednostka straży pożarna .
Do głosowania podczas plebiscytu uprawnionych było w Wawelnie 725 osób , z czego 491 , ok. 67,7 % , stanowili mieszkańcy ( w tym 457 , ok. 63,0 % całości , mieszkańcy urodzeni w miejscowości ) .
Oddano 709 głosów ( ok. 97,8 % uprawnionych ) , w tym 709 ( 100,0 % ) ważnych ; za Niemcami głosowało 648 osób ( ok. 91,4 % ) , a za Polską 61 osób ( ok. 8,6 % ) .
W 1933 r. liczba mieszkańców wynosiła 1139 , natomiast w 1939 r. – 1166 .
W tym też roku ustanowiono w miejscowości samodzielną parafię pod wezwaniem Najświętszego Serca Pana Jezusa .
Przedtem Wawelno było częścią Parafii św .
Wawrzyńca w Dąbrowie , mimo że kilka lat wcześniej wybudowano kościół na fundamentach starej szkoły .
Po II wojnie światowej liczba mieszkańców miejscowości nie uległa większym zmianom – w 1960 r. wynosiła ona 1171 , a obecnie 1152 osoby .
W Wawelnie znajduje się m.in .
pomnik poświęcony mieszkańcom miejscowości poległym w I wojnie światowej oraz liczne zabudowania z przełomu XIX i XX wieku .
Herb .
Na starych pieczęciach gminnych zachował się dawny herb miejscowości – był on dość nietypowy i przedstawiał serce ze skrzydłami , na którym siedział ptak .
Obecnie Wawelno używa herbu gminy Komprachcice .
Transport .
Z Opola możliwy jest dojazd autobusami miejskimi linii nr 16 oraz wybranymi kursami linii nr 8 .
