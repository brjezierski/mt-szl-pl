Różnica pomiędzy statystykami bozonów i fermionów istnieje wyłącznie przy dużych gęstościach – kiedy ich funkcje zachodzą na siebie .
W niskich gęstościach , obydwie statystyki redukują się do statystyki Maxwella-Boltzmanna , więc zarówno bozony i fermiony zachowują się jak cząsteczki klasyczne .
