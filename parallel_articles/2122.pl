Bozony
Bozony ( od nazwiska fizyka Satyendra Bose ) – cząstki posiadające całkowity spin .
Większość bozonów to cząstki złożone , jednakże 12 z nich ( tak zwane bozony cechowania ) są cząstkami elementarnymi , niezłożonymi z mniejszych cząstek ( cząstki fundamentalne ) .
Właściwości bozonów .
Każda cząstka jest bozonem lub fermionem , zależnie od posiadanego spinu – twierdzenie statystyki spinowej narzuca wynikającą z niego statystykę kwantową , która odróżnia fermiony od bozonów .
Zgodnie z modelem standardowym fermiony są cząstkami elementarnymi „ materii ” , natomiast bozony przenoszą oddziaływania .
W związku ze spinem całkowitym ( twierdzenie o związku spinu ze statystyką ) , nieoddziałujące bozony podlegają statystyce Bosego-Einsteina .
Jedną z jej konsekwencji jest istnienie kondensatu Bosego-Einsteina , „ piątego ” stanu skupienia materii , w którym dowolna liczba bozonów może dzielić ten sam stan kwantowy .
Oddziaływania wirtualnych bozonów z rzeczywistymi fermionami nazywamy oddziaływaniami podstawowymi .
Zachowanie pędu w tych oddziaływaniach objawia się matematycznie we wszystkich znanych siłach .
Bozony uczestniczące w tych oddziaływaniach nazywamy bozonami cechowania .
Są to :
Cząstki złożone z kilku innych cząstek ( tak jak protony lub neutrony ) mogą być zarówno fermionami , jak i bozonami , zależnie od ich całkowitego spinu .
Z tego powodu wiele jąder atomowych jest bozonami .
