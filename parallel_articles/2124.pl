Język protobułgarski
Język protobułgarski ( `` Bolğar '' , ) – wymarły język zaliczany tradycyjnie do rodziny tureckiej .
Posługiwali się nim Protobułgarzy w IX wieku .
Język ten cechowało zjawisko rotacyzmu ( przejścia określonej głoski w określonej pozycji w głoskę ‘ r ’ ) i lambdalizmu ( zamiany w ‘ l ’ ) .
Z języka tego napłynęła do współczesnej bułgarszczyzny nieznaczna liczba wyrazów , np .
`` бъбрек '' ( nerka ) czy `` кумир '' ( bożek , idol ) .
