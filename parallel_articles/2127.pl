Język bośniacki
Język bośniacki ( bośn .
/ ) – język z grupy zachodniej języków południowosłowiańskich , używany głównie przez Boszniaków .
W literaturze lingwistycznej klasyfikowany jest jako jedna z czterech odmian standardowych policentrycznego języka serbsko-chorwackiego .
Język bośniacki ma status urzędowego w Bośni i Hercegowinie , obok chorwackiego i serbskiego .
Jest również objęty statusem języka regionalnego w Serbii i Kosowie i dopuszcza się go do użytku urzędowego w Czarnogórze .
