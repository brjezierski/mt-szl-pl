Uroczystość Najświętszego Ciała i Krwi Chrystusa
Uroczystość Najświętszego Ciała i Krwi Chrystusa ( ) , pot .
Boże Ciało – uroczystość liturgiczna w Kościele katolickim dla uczczenia Jezusa Chrystusa w Najświętszym Sakramencie , obchodzona jest w czwartek po oktawie Zesłania Ducha Świętego , a we Włoszech w niedzielę po uroczystości Trójcy Świętej .
Jest świętem nakazanym .
Po raz pierwszy uroczystość obchodzona była w Leodium ( Liège ) w 1246 , a w 1317 ustanowiona jako święto dla całego Kościoła zachodniego .
W Polsce zostało wprowadzone przez krakowskiego biskupa Nankera na synodzie w Krakowie w 1320 .
W Kościele unickim święto zostało wprowadzone na synodzie zamojskim w 1720 .
Zewnętrzną formę święta stanowią procesje prowadzone do 4 ołtarzy .
Procesyjnie przenoszona jest monstrancja z Najświętszym Sakramentem .
