Bożnowice
Bożnowice ( niem .
`` Berzdorf '' ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie ząbkowickim , w gminie Ziębice .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa wałbrzyskiego .
