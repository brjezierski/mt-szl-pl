Brandenburg an der Havel
Brandenburg an der Havel ( powszechnie stosowna jest również forma skrócona Brandenburg ; hist .
lub `` Branibórz '' , hist . )
– miasto na prawach powiatu w Niemczech w kraju związkowym Brandenburgia , położone nad rzeką Hawelą .
Ponad 20 % powierzchni miasta zajmują wody .
Miasto Brandenburg nadało nazwę marchii brandenburskiej , późniejszej prowincji Brandenburgia a także dzisiejszemu krajowi związkowemu .
