Bratowice
Bratowice – wieś w Polsce położona w województwie dolnośląskim , w powiecie wrocławskim , w gminie Żórawina , należąca do sołectwa Bratowice-Zagródki .
Nazwa .
Pierwsza zachowana wzmianka na temat wsi pochodzi z łacińskiego dokumentu z 1337 roku notująca pierwotną nazwę `` Barotowicz '' .
Później notowana również w 1360 `` Barothowicz '' , 1372 `` Baratow '' , 1669 `` Barutwitz '' , 1736 `` Parothwitz '' .
Nazwa wywodzona jest od staropolskiego określenia `` brata '' lub imienia `` Bartłomiej '' .
Oba wywody podaje Paul Hefftner w swojej pracy o nazwach miejscowości ziemi wrocławskiej pt .
`` Ursprung und Bedeutung der Ortsnamen im Stadt und Landkreise Breslau '' .
W XIX wieku nazwa została zgermanizowana przez Niemców początkowo na `` Barothwitz '' , a później `` Barottwitz '' .
Ze względu na polskie pochodzenie po dojściu do władzy w Niemczech nazistów 26 stycznia 1937 roku zmieniona przez władze III Rzeszy na `` Schmücken '' ( od nazwy karczmy jaka stała przy wiązowskiej drodze ) .
Historia .
Od średniowiecza do 1810 roku wieś pozostawała w rękach właścicieli prywatnych .
Wieś wymieniona w kościelnej ankiecie z 1814 roku jako wieś z ludnością mówiącą po polsku ( w kościele językiem kazań był język polski i niemiecki ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa wrocławskiego .
