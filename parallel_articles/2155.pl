Brunszwik
Brunszwik ( niem .
`` Braunschweig '' , dolnosaks .
`` Brunswiek '' ) – miasto na prawach powiatu w środkowej części Niemiec , w kraju związkowym Dolna Saksonia , na północy masywu górskiego Harz , nad rzeką Oker .
Ważny ośrodek przemysłowy , rozwinięty zwłaszcza przemysł maszynowy , środków transportu ( głównie autobusy i samochody ciężarowe ) , elektrotechniczny , elektroniczny , metalowy , precyzyjny , spożywczy , instrumentów muzycznych ; węzeł komunikacyjny ; politechnika ( założona w 1745 ) z biblioteką i inne wyższe szkoły ( pedagogiczna , sztuk pięknych ) , instytuty naukowe , wydawnictwa .
Historia .
Średniowiecze .
Już w IX wieku na terenie miasta w rejonie dzisiejszego Targu Węglowego `` ( Kohlmarkt ) '' powstała osada , późniejsze Stare Miasto `` ( Altstadt ) '' .
Według tradycji Brunszwik został założony przez Brunona II .
