Brazzaville
Brazzaville – największe miasto , stolica administracyjna i finansowa Konga , położone nad rzeką Kongo .
Miasto zamieszkuje około 1,5 mln osób , co stanowi ponad 30 % ludności tego kraju .
Naprzeciwko Brazzaville , po drugiej stronie rzeki , znajduje się Kinszasa ( stolica Demokratycznej Republiki Konga ) , która posiada prawie 12 milionów mieszkańców .
Konurbację obu miast zamieszkuje ponad 13 milionów ludzi .
Jest to jedyny przypadek na świecie , kiedy stolice dwóch państw leżą naprzeciwko siebie na dwóch przeciwnych brzegach rzeki .
