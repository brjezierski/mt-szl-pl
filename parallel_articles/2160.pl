Bracia Marx
Bracia Marx , bracia Marksowie ( ang .
`` Marx Brothers '' ) – rodzeństwo aktorów teatralnych i filmowych :
Rodzicami braci byli żydowscy emigranci : Sam Marx ( ur .
1859 , zm .
1933 ) z Alzacji i Miene Schönberg ( ur .
1864 , zm .
1929 ) , niemiecka emigrantka z Dornum .
Rodzice mieli jeszcze jednego syna , Manfreda ( ur .
1885 ) , który zmarł w siódmym miesiącu życia .
Kariera artystyczna .
Karierę artystyczną zaczęli od występów w wodewilach , gdzie już wcześniej zdobył sławę ich wuj Al Shean ( Gallagher and Shean ) .
Pierwszym z braci , który pojawił się w 1905 na scenie był Groucho .
Do niego dołączył w 1907 Gummo , z którym razem występowali jako dwójka z trójki śpiewaków grupy „ The Three Nightingales ” ( ang .
Trzy słowiki ) .
Trzecim członkiem grupy była Mabel O ’ Donnell , po odejściu której w 1908 razem z braćmi zaczął występować Harpo .
Razem z nim do grupy , która zmieniła nazwę na „ The Four Nightingales ” ( ang .
Cztery słowiki ) dołączył Lou Levy .
Grupa poszerzyła się w 1910 o następne dwie osoby : ich matkę Minnie i ciotkę Hannah , a także zmieniła nazwę na „ The Six Mascots ” ( ang .
Sześć maskotek ) .
Zaczęli wzbogacać stopniowo występy o elementy komediowe , wcielając się w charakterystyczne dla nich później role .
Na twarzy Groucha pojawiły się namalowane wąsy , Chico zaczął mówić z włoskim akcentem , a Harpo przestał mówić .
Pseudonimy artystyczne wymyślone przez Arta Fishera podczas partyjki pokera rozgrywanej z nimi :
Gummo wystąpił z grupy w 1918 , a jego miejsce zajął Zeppo .
Po raz pierwszy na Broadwayu pojawili się 19 maja 1924 w sztuce pod tytułem „ I ’ ll Say She Is ” , a półtora roku później 8 grudnia 1925 wystawili kolejną „ The Cocoanuts ” .
W trakcie wystawiania następnej sztuki ( „ Animal Crackers ” ) podpisali kontrakt z wytwórnią Paramount na realizację filmowej wersji „ The Coconauts ” .
W ciągu trzech kolejnych lat wyprodukowali dla Paramount jeszcze trzy filmy „ Monkey Business ” , „ Horse Feathers ” i „ Duck Soup ” .
Ten ostatni American Film Institute umieścił na liście stu najlepszych amerykańskich filmów na miejscu 85 .
Po wygaśnięciu umowy z Paramountem w 1934 podpisali nową , tym razem z MGM , jednak już bez Zeppa , który zrezygnował z kariery aktorskiej .
Po nakręceniu 5 filmów dla MGM postanowili zakończyć współpracę i nakręcili film , który miał być ich ostatnim wspólnym „ The Big Store ” .
Po nakręceniu go przez pewien czas pracowali nad własnymi karierami .
Jednak po kilku latach nakręcili wspólnie jeszcze dwa filmy „ A Night in Casablanca ” i „ Love Happy ” .
