Braniewo
Braniewo ( ) – miasto na Warmii w województwie warmińsko-mazurskim , siedziba powiatu braniewskiego .
Położone jest na Równinie Warmińskiej , nad rzeką Pasłęką w pobliżu ujścia do Zalewu Wiślanego .
Najstarsze miasto na Warmii i jej pierwsza stolica .
Dawniej port morski i miasto hanzeatyckie .
W latach 1466–1772 w obrębie Korony Królestwa Polskiego .
W latach 1954–1972 miasto było siedzibą władz gromady Braniewo .
W latach 1975–1998 miasto administracyjnie należało do województwa elbląskiego .
Pod względem historycznym Braniewo leży zarówno na Warmii biskupiej , jak i na obszarze Warmii plemiennej .
