Brescello
Brescello – miejscowość i gmina we Włoszech , w regionie Emilia-Romania , w prowincji Reggio Emilia .
Według danych na rok 2007 gminę zamieszkiwało 5228 osób , 218 os./km² .
Historia .
Początki osadnictwa na tym terenie datują się na VII w. p.n.e .
Wtedy to plemiona galijsko-germańskie zbudowały w dolinie Padu swoje siedziby .
Ten kompleks budowli nosił nazwę `` Brixellum '' .
Pomimo silnego oporu osiadłych tu plemion , ok. 220 tereny te zostały zajęte przez wojska Republiki rzymskiej .
Od tego czasu Brescello stało się rzymską kolonią , co odzwierciedliło się w przekształceniach urbanistycznych , typowych dla osad budowanych przez Rzymian .
