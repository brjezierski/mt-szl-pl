Brescia ( stacja kolejowa )
Brescia – stacja kolejowa w Bresci , w regionie Lombardia , we Włoszech .
Znajduje się na linii Mediolan - Wenecja .
Jest to również stacja początkowa dla linii wychodzących w kierunku Bergamo , Cremony i Parmy , jak również linii FERROVIENORD do Edolo .
Jest to stacja średniej wielkości , obsługująca 9 mln pasażerów rocznie .
Należy do projektu Centostazioni , co przyczyniło się do modernizacji budynku i infrastruktury dworca .
