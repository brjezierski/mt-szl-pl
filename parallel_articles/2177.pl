Bressanone
Bressanone ( niem .
Brixen , łac .
`` Bressanon '' , lad .
`` Persenon '' lub `` Porsenù '' ) – miejscowość i gmina we Włoszech , w regionie Trydent-Górna Adyga , w prowincji Bolzano , położone nad ujściem rzeki Rienz ( Rienza ) do Eisack ( Isarco ) .
Historia .
Po raz pierwszy zostało wspomniane w 901 jako `` Brixen '' .
Biskupstwo zostało przeniesione do Brixen z Seben przez św .
Albuina ( zm .
1006 ) .
W 1039 biskup Brixen , Poppo , został przez cesarza Henryka III uczyniony papieżem , którym Pappo był tylko 23 dni .
W XI w. Brixen zostało stolicą kościelnego księstwa , walczącego z sąsiadem – hrabstwem Tyrolu .
W 1115 postawiono kamienne wały miejskie .
Za panowania w Tyrolu Otto III ustalono granicę z biskupstwem na rzekach Adyga i Avisio .
Biskupstwo zostało zsekularyzowane w 1803 i przyłączone do Austrii .
Przyszedł tutaj na świat włoski himalaista Reinhold Messner , a także zbrodniarz wojenny Josef Schwammberger , oberscharführer SS , komendant obozów w Rozwadowie , Mielcu i getta w Przemyślu .
Na wsi , niedaleko Brixen , urodziła się babka papieża Benedykta XVI ( Maria Tauber-Peintner , ur .
29 czerwca 1855 ) , która później przeniosła się do Mühlbach , w gminie niemieckiej Kiefersfelden .
Demografia .
Liczba mieszkańców gminy wynosiła 20 512 ( dane z roku 2009 ) .
Język niemiecki jest językiem ojczystym dla 73,13 % , włoski dla 25,65 % , a ladyński dla 1,23 % mieszkańców ( 2001 ) .
