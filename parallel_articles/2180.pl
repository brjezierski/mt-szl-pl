Brześć
Brześć , do 1923 Brześć Litewski , Brześć nad Bugiem ( , , `` Brest '' , `` Bieraście '' ; , , `` Brest '' , `` Bieriestje '' ; , ) – miasto na Białorusi , u ujścia Muchawca do Bugu , siedziba administracyjna obwodu brzeskiego i rejonu brzeskiego .
Ośrodek przemysłowy , węzeł kolejowy i drogowy na granicy z Polską , port rzeczny , port lotniczy ; uniwersytet ( 1995 ) , politechnika ( 1989 ) ; ośrodek kultury polskiej na Białorusi , polski konsulat generalny ; 339,7 tys .
mieszkańców ( 2020 ) .
Miasto królewskie , położone w końcu XVIII wieku w powiecie brzeskolitewskim województwa brzeskolitewskiego .
Miejsce obrad Sejmu Wielkiego Księstwa Litewskiego .
Miejsce obrad sejmików ziemskich powiatu brzeskolitewskiego od XVI wieku do pierwszej połowy XVIII wieku .
Do 1945 w Polsce , od 1921 stolica województwa poleskiego , siedziba powiatu brzeskiego .
Dane ogólne .
Brześć jest szóstym pod względem liczby mieszkańców i drugim pod względem zajmowanego obszaru miastem Białorusi .
Duży ośrodek przemysłowy ( m.in .
produkcja maszyn i mebli ) , kulturalny ( muzeum twierdzy brzeskiej ) i naukowy ( uczelnie ) .
Działa tam także polski konsulat generalny .
Miasto jest siedzibą prawosławnej eparchii brzeskiej i kobryńskiej .
Układ przestrzenny .
Miasto Brześć położone jest na prawym brzegu Bugu i Muchawca , u zbiegu tych dwóch rzek .
Przestrzeń miasta obecnie obejmuje 14 526 ha .
Historyczne centrum miasta zajęte przez twierdzę brzeską , zbudowaną według projektu inż .
gen. K. Oppermana , podzieloną obecnie granicami Polski i Białorusi .
Kobryński forsztadt twierdzy albo miasto XIX-wieczne składa się z regularnej sieci ulic i zwartej zabudowy z XIX i XX wieku , objęty obecnie ochroną jako zabytek sztuki urbanistycznej .
Pierwszy powojenny generalny plan Brześcia ułożony w 1947 .
Obecnie odbywa się korygowanie Generalnego Planu Brześcia z 2003 .
Głównymi ulicami przebiegającymi ze wschodu na zachód są : Sergo Ordżonikidze , Adama Mickiewicza , Puszkińska , Władimira Majakowskiego , Nikołaja Gogola ( dawna Tadeusza Kościuszki ) , Siemiona Budionnego , Feliksa Dzierżyńskiego , Moskiewska ( dawna Jagiellońska ) , Internacjonalna ( dawna Szpitalna ) .
Główne ulice przebiegające z północy na południe to : Włodzimierza Lenina , Karola Marksa , Komsomolska , Pograniczników Sowieckich , 17 Września , Sowiecka , Waleriana Kujbyszewa , Dmitrija Karbyszewa , Bulwar Kosmonautów , Tarasa Szewczenki .
