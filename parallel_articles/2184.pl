Breukelen
Breukelen – miasto w Holandii , w prowincji Utrecht .
W 2011 roku liczyło 9210 mieszkańców .
To od nazwy tej miejscowości nazwana została holenderska osada nad East River , która z czasem rozrosła się i stała się częścią Nowego Jorku jako okręg Brooklyn .
W miejscowości urodził się aktor Rutger Hauer .
