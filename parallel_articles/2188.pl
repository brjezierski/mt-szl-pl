Brian Lumley
Brian Lumley ( ur .
2 grudnia 1937 w Horden ) – angielski pisarz .
Zafascynowany w młodości twórczością H.P .
Lovecrafta , pisywał początkowo opowiadania a następnie powieści z gatunku horroru i mystery .
Nie zyskały one większego rozgłosu .
Przełom w jego pisarskiej karierze nastąpił po wydaniu książki `` Psychomech '' ( 1984 ) , zauważonej przez krytykę .
Do dziś najbardziej znanym dziełem pisarza pozostaje zapoczątkowany w 1986 roku cykl `` Nekroskop '' .
Lumley łączy w swej twórczości elementy mitologii Cthulhu , zjawisk paranormalnych ( ESP ) , a w cyklu `` Nekroskop '' także ludowe mity o wampirach , wilkołakach , „ złym oku ” i inne .
Po części usystematyzował je w dość spójnej , choć odległej od rzeczywistości wizji świata .
Ze względu na wątki udziału tajnych rządowych agencji w zmaganiach ludzkości z istotami o nadludzkich możliwościach , bywa nazywany „ Ludlumem horroru ” .
