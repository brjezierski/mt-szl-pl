Kody pocztowe w różnych krajach .
Bułgaria .
W Bułgarii kod pocztowy składa się z czterech cyfr ( np .
1000 Sofia )
Belgia .
Również w Belgii kod pocztowy składa się zawsze z 4 cyfr ( np .
1000 Bruksela )
Chorwacja .
Chorwackie kody pocztowe składają się z 5 cyfr ( np .
10000 Zagrzeb )
Francja .
We Francji dwie pierwsze cyfry są numerem departamentu – departamenty są jednocześnie okręgami pocztowymi .
Pierwsze dwie cyfry kodu pocztowego – numeru departamentu były do niedawna wyróżnikiem w samochodowych numerach rejestracyjnych .
W nowych numerach rejestracyjnych , numer departamentu nie stanowi już jego części i można go sobie wybrać .
Holandia .
W Holandii kod pocztowy składa się z 4 cyfr i 2 liter ( np .
1011 AB Amsterdam )
Irlandia .
W Irlandii kod pocztowy składa się z siedmiu alfanumerycznych znaków – pierwsze trzy znaki wskazują na okręg pocztowy , pozostałe cztery są unikalnym identyfikatorem budynku lub mieszkania .
