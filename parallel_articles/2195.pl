Kanada .
Kanadyjski kod pocztowy jest złożony z sześciu znaków w formacie : X9X 9X9 , gdzie X jest literą a 9 cyfrą .
Trzeci i czwarty znak jest oddzielony spacją .
Dla przykładu : K1A 0B1 jest przypisany dla poczty w Ottawie , natomiast kod H0H 0H0 jest zarezerwowany dla Świętego Mikołaja .
Niemcy .
W Niemczech kod pocztowy składa się z 5 cyfr .
Pierwsza cyfra oznacza okręg pocztowy .
Przykłady kodów pocztowych :
12524 – kod w Berlinie
20355 – kod w Hamburgu
15236 – kod we Frankfurcie nad Odrą
01219 – kod w Dreźnie
45327 – kod w Essen
Po raz pierwszy oznakowania miejscowości cyframi zastosowała w 1853 r. poczta Thurn und Taxis .
W 1941 roku wprowadzono w Rzeszy Niemieckiej dwucyfrowy system kodów pocztowych .
W 1962 roku w RFN i w 1965 roku w NRD wprowadzono odrębne systemy kodowań , składające się z czterocyfrowego kodu miast .
1 lipca 1993 roku wprowadzono obowiązujący obecnie wspólny system kodów pocztowych .
Pokazany jest on na mapie obok .
Polska .
W Polsce kody pocztowe mają format dd-ddd ( d oznacza cyfrę ) i umieszcza się je z lewej strony nazwy miejscowości .
Pierwsza cyfra określa okręg pocztowy .
Przed wejściem w życie 1.01.2013 r. ustawy Prawo pocztowe , cały PNA był przypisany albo do obszaru obsługiwanego przez urząd pocztowy , któremu nadano taki sam kod , albo w tzw .
miastach wydzielonych , do poszczególnych ulic i domów .
Wówczas likwidując urzędy pocztowe lub zmieniając ich obszar działania , zmieniano kod pocztowy miejscowości .
Po wejściu ustawy Prawo pocztowe , opublikowano kody PNA przypisane do danej miejscowości lub ich części .
Nie dokonuje się już zmian kodów spowodowanych reorganizacją obszarów działania placówek Poczty Polskiej .
Stany Zjednoczone .
Kod pocztowy nazywany jest w Stanach Zjednoczonych ZIP ( Zone Improvement Plan ) Code .
Kody są 5-cyfrowe bez dywizu .
Pierwsza cyfra odpowiada pewnej grupie stanów jej przypisanych .
Druga i trzecia cyfra odpowiada poszczególnym regionom ( lub wielkim miastom ) w danej grupie .
Z reguły pierwsze trzy cyfry określają urząd odpowiedzialny za sortowanie poczty dla całego regionu .
Dany urząd może posiadać więcej niż jeden trzycyfrowy numer .
Czwarta i piąta cyfra wyznaczają małe miasteczka , wioski lub dzielnice/przedmieścia wielkich miast .
Przykład : 43209 ( 4=Ohio,32=Columbus,09=Bexley )
Pierwszy kod dla danego sektora najczęściej przydzielany jest największemu miastu w tym regionie .
Kolejne kody zwykle korespondują alfabetycznie z nazwami kolejnych miast , miasteczek i wiosek .
Wielka Brytania .
Brytyjski kod pocztowy składa się z dwóch alfanumerycznych sekcji : pierwsza , o długości dwóch do czterech znaków , identyfikuje okręg pocztowy ; druga , o długości trzech znaków , identyfikuje jednostkę obejmującą co najwyżej 100 sąsiadujących nieruchomości ( na przykład odcinek gęsto zabudowanej ulicy , lub kilka sąsiadujących , luźno zabudowanych ulic ) .
W większości przypadków , podanie tylko numeru ( lub nazwy ) budynku i kodu pocztowego wystarcza do prawidłowego zidentyfikowania adresu .
