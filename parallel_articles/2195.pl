Kod pocztowy
Kod pocztowy ( pocztowy numer adresowy ) – ciąg cyfr ( rzadziej liter i cyfr ) dodawany do adresu , mający ułatwiać sortowanie przesyłek .
Formaty i zasady umieszczania kodów pocztowych są różne w różnych krajach .
