Znaczek pocztowy
Znaczek pocztowy – pocztowy znak wartościowy , służący do rozliczania należności za usługi pocztowe .
Przeważnie w postaci małego papierowego prostokąta przyklejanego do koperty .
Znaczek pocztowy zalicza się do druków akcydensowych .
Charakterystyka .
Znaczek pocztowy jest drukiem ścisłego zarachowania i musi posiadać widoczną wartość nominalną .
Wykonywany jest różnymi technikami drukarskimi ( typografia , litografia , offset , staloryt , rotograwiura ) i na różnych rodzajach papieru ( często ze znakami wodnymi ) na podstawie projektu grafika .
Znaczki mogą przybierać różne kształty , nie tylko prostokąta ( w tym kwadratu ) .
Spotyka się znaczki owalne , trójkątne i pięciokątne .
Sierra Leone i Tonga wypuściły samoprzylepne znaczki w kształcie owoców .
Różne są również materiały , z których wykonuje się znaczki .
Można trafić na znaczki z folii ( nawet srebrnej lub złotej ) , plastiku , a nawet drewna .
Corocznie 9 października obchodzony jest Dzień Znaczka Pocztowego .
