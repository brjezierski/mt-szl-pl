Brigitte Bardot
Brigitte Anne Marie Bardot ( ur .
28 września 1934 w Paryżu ) – francuska modelka , piosenkarka i aktorka filmowa , symbol seksapilu w latach 50. i 60 .
XX wieku , jedna z największych gwiazd francuskiego kina ; później także działaczka na rzecz ochrony praw zwierząt .
Życiorys .
Wczesne lata .
Brigitte Bardot urodziła się jako jedna z dwóch córek Louisa Bardot i Anne Marie Mucel .
Ojciec , dyplomowany inżynier , prowadził rodzinny interes .
Matka zapisała córki Brigitte i Marie-Jeanne do szkoły baletowej .
Marie-Jeanne ostatecznie zrezygnowała z lekcji tańca , natomiast Brigitte skoncentrowała się na ćwiczeniach .
W 1947 została przyjęta do Konserwatorium Paryskiego i przez trzy lata , wraz z Leslie Caron , uczęszczała do klasy baletowej rosyjskiego choreografa Borisa Kniaziewa .
