Briańsk
Briańsk ( ros .
Брянск ) – miasto obwodowe w zachodniej części Rosji , nad Desną , w pobliżu granicy z Białorusią i Ukrainą .
Leży na obszarze historycznej Siewierszczyzny .
Ważny węzeł kolejowy , ośrodek przemysłowy , naukowy ( 4 szkoły wyższe ) i kulturalny ( 2 muzea ) .
Według spisu powszechnego z 2010 roku miasto liczyło 415 721 , a okręg miejski 435 245 mieszkańców .
W 2015 roku było to odpowiednio 407 256 i 426 255 mieszkańców .
Części miasta .
Miasto jest podzielone na 4 rejony ( odpowiednik polskich dzielnic ) .
Administracji miejskiej Briańska podlegają również 3 osiedla typu miejskiego : Radica-Kryłowka ( ros : Радица-Крыловка ) podlegające administracji rejonu bieżyckiego ( 3 526 mieszkańców w 2010 roku ) , Bolszoje Połpino ( ros : Большое Полпино ) podlegające administracji rejonu wołodarskiego ( 6 356 mieszkańców w 2010 roku ) i Biełyje Bieriega ( ros : Белые Берега ) podlegające administracji rejonu fokińskiego ( 9 642 mieszkańców w 2010 roku ) .
Łącznie z podlegającymi miastu osiedlami miejskiego okręg miejski Briańsk liczył w 2010 roku 435 245 mieszkańców .
Historia .
Pierwsza wzmianka o mieście notowana w roku 1146 , ale jako gród Briańsk istniał już od końca X wieku .
W 1310 r. zostało najechane przez Mongołów .
Od roku 1356 do końca XV wieku w granicach Wielkiego Księstwa Litewskiego .
