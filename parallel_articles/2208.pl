Brno
Brno ( niem .
`` Brünn '' , łac .
`` Bruna '' , dawna nazwa polska `` Berno '' ) – miasto statutarne na Morawach w Czechach , położone w południowo-wschodniej części kraju u zbiegu rzek Svratki i Svitavy .
Na początku 2020 roku , z liczbą mieszkańców wynoszącą ponad 380 tys. , Brno zajmowało drugie miejsce pod względem liczby ludności ( po Pradze ) w Republice Czeskiej .
Największe miasto Moraw , stolica kraju południowomorawskiego i okręgu terytorialnego kraj południowomorawski .
Miasto jest ważnym ośrodkiem kulturalnym ( teatry , filharmonia , międzynarodowe festiwale muzyczne ) , naukowym ( 6 uczelni , w tym Uniwersytet Masaryka i Uniwersytet Techniczny ) i turystycznym .
Mimo iż Brno nie jest stolicą Czech , mają w nim siedzibę centralne , najważniejsze instytucje wymiaru sprawiedliwości : sąd konstytucyjny , Sąd Najwyższy i ombudsman Republiki Czeskiej .
Brno jest historyczną stolicą Moraw .
Historia .
Początki miasta sięgają epoki brązu i żelaza .
Najprawdopodobniej już między V a VII wiekiem powstała w obszarze dzisiejszego miasta warownia słowiańska , mająca charakter ośrodka administracyjnego .
Natomiast gród , który rozwinął się we współczesne miasto , został założony przez księcia Brzetysława I między rokiem 1021 a 1034 na wzgórzu Petrov .
