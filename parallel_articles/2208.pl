Mikołaja .
Pierwsza pisemna wzmianka o ośrodku została zamieszczona w 1091 roku w kronice Kosmasa .
W 1243 roku król czeski Wacław I nadał miastu prawa miejskie i przywilej miasta królewskiego .
Kilkadziesiąt lat później król Przemysł Ottokar II stworzył tu zamek , który dał początek późniejszej twierdzy Špilberk i stanowił podstawę systemu umocnień miejskich Brna .
Od 1349 roku miasto było siedzibą margrabiów morawskich .
W 1376 roku Brno otrzymało sołectwo .
W latach 1428 i 1430 bezskutecznie oblegali je husyci .
W 1641 roku została tu na trwałe przeniesiona z Ołomuńca stolica Moraw i w ciągu kolejnych lat wojny trzydziestoletniej ( 1643 i 1645 ) miasto , dzięki umiejętnościom komendanta Louisa Raduit de Souches , z sukcesem odpierało ataki wojsk szwedzkich pod dowództwem generała Lennarta Torstenssona .
Konsekwentnie umacniane , w 1742 roku było bez powodzenia oblegane przez wojska pruskie .
