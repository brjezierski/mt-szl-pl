Brody ( rejon mołodecki )
Brody ( biał .
`` Броды '' , ros .
`` Броды '' ) – wieś na Białorusi , w obwodzie mińskim , w rejonie mołodeczańskim , w sielsowiecie Chożów .
Dawniej używana nazwa – Brody Bierezowieckie .
Historia .
W czasach zaborów zaścianek w gminie Horodek , w powiecie wilejskim , w guberni wileńskiej Imperium Rosyjskiego .
W latach 1921–1945 zaścianek leżał w Polsce , w województwie wileńskim , w powiecie wilejskim , od 1927 roku w powiecie mołodeczańskim , w gminie Gródek .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 41 osób , 8 było wyznania rzymskokatolickiego a 33 prawosławnego .
