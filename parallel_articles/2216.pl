Bromölla ( stacja kolejowa )
Bromölla ( szwedzki : `` Bromölla station '' ) – stacja kolejowa w Bromölla , w regionie Skania , w Szwecji .
Znajduje się na wschód od rzeki Skräbeån .
Stacja jest obsługiwana przez pociągi Öresundståg między Kopenhagą i Karlskroną .
Oryginalna stacja została otwarta w 1874 wraz z linią Kristianstad – Sölvesborg .
W 1909 stacja zmieniła swoje położone względem pierwotnej , a w 1999 zbudowano nową stację od podstaw w obecnym miejscu , na wschód od starej .
