Bronisław Cieślak
Bronisław Emil Cieślak ( ur .
8 października 1943 w Krakowie , zm .
2 maja 2021 w Marcyporębie ) – polski aktor , dziennikarz , prezenter telewizyjny i polityk .
Odtwórca roli Sławomira Borewicza w serialu telewizyjnym `` 07 zgłoś się '' ( 1976–1987 ) i Bronisława Malanowskiego w serialu paradokumentalnym `` Malanowski i Partnerzy '' ( 2009–2016 ) .
W latach 1997–2005 poseł na Sejm III i IV kadencji .
Życiorys .
Młodość i wykształcenie .
Syn Bronisława i Marii .
Wychowywał się na krakowskim Kazimierzu .
Jego ojciec był kolejarzem , matka zajmowała się domem .
Miał dwóch braci : Jacka i Witolda .
W 1962 został absolwentem I Liceum Ogólnokształcącego im .
Bartłomieja Nowodworskiego w Krakowie .
Podczas nauki występował w klubie studenckim Pod Jaszczurami .
