Bronisław Geremek
( ur .
6 marca 1932 w Warszawie , zm .
13 lipca 2008 w Lubieniu ) – polski historyk i polityk , profesor nauk humanistycznych .
Działacz opozycji politycznej w okresie PRL .
Uczestnik obrad Okrągłego Stołu .
Poseł na Sejm X , I , II i III kadencji ( 1989–2001 ) , minister spraw zagranicznych ( 1997–2000 ) , przewodniczący Unii Wolności ( 2000–2001 ) , deputowany do Parlamentu Europejskiego VI kadencji ( 2004–2008 ) .
Kawaler Orderu Orła Białego .
Życiorys .
Dzieciństwo .
Urodził się jako Benjamin Lewertow w żydowskiej rodzinie Borucha i Szarcy Lewertowów .
Jego ojciec prowadził wytwórnię futer w Warszawie .
Miał starszego brata Israela , który po wojnie wyjechał do Izraela , a w 1951 wyemigrował do Stanów Zjednoczonych , gdzie przyjął nazwisko Jerry Lewart .
Jeden z jego dziadków , Israel , był magidem .
Nie wypowiadał się wprost o swoim pochodzeniu i dzieciństwie , które uważał za `` zamknięty kajet '' , jednak w wywiadzie-rzece udzielonym Jackowi Żakowskiemu wspominał o `` świadomości żydowskiej '' , którą w sobie wciąż nosi .
W 1940 trafił wraz z rodzicami i bratem do getta warszawskiego .
W 1942 , jak relacjonował jego brat , rodzina została przeznaczona do wywózki do obozu zagłady i trafiła na Umschlagplatz , lecz ojciec zdołał ich wykupić .
W drugiej połowie tegoż roku Bronisław Geremek uciekł z getta , podobnie jak jego brat , ojciec i matka .
Jego ojciec został jednak później schwytany i zginął w 1943 lub 1944 w KL Auschwitz .
Po opuszczeniu getta przez miesiąc ukrywał się w Warszawie u znajomego Polaka , po czym wraz z matką wyjechał do Zawichostu , gdzie pod przybranym nazwiskiem Wachlewscy ukrywali się u Stefana Gieremka , prowadząc jego sklep .
Przybrał imię Bronisław , zaś jego matka przyjęła imię Maria .
Po wojnie Stefan Gieremek ożenił się z matką Bronisława .
Cała rodzina przeniosła się do Wschowy , gdzie jego ojczym był urzędnikiem państwowym , a do 1947 starostą .
We Wschowie Bronisław Geremek ( pod nazwiskiem w brzmieniu Gieremek ) zdał małą maturę .
W 1948 przeprowadził się na warszawski Żoliborz .
W życiu dorosłym nie uważał się za Żyda .
O swoich korzeniach przypominał , gdy stawał wobec zjawiska antysemityzmu .
Wykształcenie i praca zawodowa .
W 1955 ukończył studia na Wydziale Historycznym Uniwersytetu Warszawskiego , w tym samym roku wyjechał do Stanów Zjednoczonych na stypendium Smithsonian Institution , w latach 1956–1958 odbył studia podyplomowe w École pratique des hautes études w Paryżu .
W 1960 uzyskał stopień naukowy doktora , a w 1972 habilitował się w Polskiej Akademii Nauk .
