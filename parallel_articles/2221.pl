Bronisław Opałko
Bronisław Józef Opałko ( ur .
17 lutego 1952 w Kielcach , zm .
18 sierpnia 2018 tamże ) – polski artysta kabaretowy , aktor , kompozytor i autor tekstów .
Od lat 70 .
XX w. twórca i wykonawca postaci Genowefy Pigwy .
Współpracował m.in .
z Zespołem Adwokackim Dyskrecja .
Tworzył kabaret Pigwa-Show , w którym skecze przeplatane były piosenkami kabaretowymi .
Występował na festynach , koncertach , a także ze specjalnym programem na eventach i imprezach okolicznościowych .
Życiorys .
W Wyższej Szkole Pedagogicznej w Kielcach ukończył studia na kierunku wychowanie muzyczne .
Podczas studiów występował w kabarecie „ Pod postacią ” , potem założył „ Orkiestrę do Użytku Wewnętrznego ” .
W 1986 związał się na kilka lat z kabaretem radiowym Zespół Adwokacki Dyskrecja , dla którego w latach 1986–1989 nagrał wiele piosenek oraz występował w skeczach .
W 1987 założył własny kabaret „ Pigwa Show ” .
