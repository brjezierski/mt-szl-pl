Broszków
Broszków – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Kotuń .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Antoniego w Kotuniu .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Dane ogólne .
Wieś jest położona 12 kilometrów od Siedlec i 90 kilometrów od Warszawy .
W Broszkowie znajduje się remiza strażacka ( OSP Broszków ) i XIX-wieczny park oraz Rezerwat przyrody Stawy Broszkowskie .
Przebiega tędy droga krajowa nr 2 , będąca polską częścią międzynarodowego szlaku komunikacyjnego E30 z Cork ( Irlandia ) do Omska ( Rosja ) .
Historia .
Najstarsze znane zapiski o Broszkowie pochodzą z dokumentu z 1428 r. , w którym jest on wymieniony jako majątek będący w posiadaniu rodziny należącej do szlachty mazowieckiej , pochodzącej z Prusów .
Od Broszkowa wzięła swe nazwisko rodzina Broszkowskich , pieczętująca się najpierw herbem Prus , potem herbem Brochwicz .
