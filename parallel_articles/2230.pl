Browiniec Polski
Browiniec Polski ( dodatkowa nazwa w j. niem .
Probnitz , przed 1918 r. `` Polnisch Probnitz '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Biała .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 139 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 5 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy gminy Biała z gminą Lubrza .
Należy do Euroregionu Pradziad .
Leży na obszarze 405,97 ha .
Ma charakter rolniczy .
Głównymi uprawami są tutaj buraki cukrowe , rzepak i pszenica .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
