Browky Perszi
Browky Perszi ( ) – wieś na Ukrainie , w obwodzie żytomierskim , w rejonie berdyczowskim , w hromadzie Andruszówka , nad rzeką Kamjanką .
W 2001 roku liczyła 961 mieszkańców .
Wieś położona jest ok. 110 km na południowy zachód od Kijowa .
Od zachodu graniczy z Łebedyncami , od południa z Jareszkami , a od północy z Horodyszczem .
Historia .
Pierwsza wzmianka o miejscowości pochodzi z 1617 roku .
Wieś wchodziła w skład starostwa białocerkiewskiego w województwie kijowskim , a po rozbiorach – wołości Browki w ujeździe skwirskim , w guberni kijowskiej .
Stanowiła własność książąt Lubomirskich , następnie Rohozińskich .
Do 1918 roku wieś była własnością Ściborów-Rylskich .
W XIX wieku powstała stacja kolei oraz linia do Kijowa .
W 1870 roku wieś liczyła 723 prawosławnych oraz 39 katolików .
W miejscowości znajdowała się cerkiew parafialna .
Dwór .
We wsi znajdują się ruiny parterowego dworu krytego dachem czterospadowym , wybudowanego na wysokiej oficynie ; od frontu portyk z sześcioma kolumnami podtrzymującymi trójkątny fronton .
Kolumnady łączyły dwór z bocznymi oficynami .
Do wejścia prowadziły schody .
