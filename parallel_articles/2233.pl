Bruce Lee
Bruce Lee ( 李小龍 pinyin `` Lǐ Xiăolóng '' ; urodz .
`` Lee Jun-fan '' ( 李振藩 pinyin `` Lǐ Zhènfān '' ) ; ur .
27 listopada 1940 w San Francisco , zm .
20 lipca 1973 w Hongkongu ) – amerykańsko-hongkoński aktor , reżyser , mistrz i instruktor sztuk walk , filozof pochodzenia chińskiego .
Był założycielem Jeet Kune Do , hybrydowej filozofii sztuk walki czerpiącej z różnych dyscyplin walki , której często przypisuje się utorowanie drogi dla współczesnych mieszanych sztuk walki ( MMA ) .
Lee jest uważany przez komentatorów , krytyków , media i innych twórców sztuk walki za najbardziej wpływowego mistrza sztuk walki wszech czasów i ikonę popkultury XX wieku , która wypełniła lukę między Wschodem a Zachodem .
Przypisuje mu się pomoc w zmianie sposobu przedstawiania Azjatów w amerykańskich filmach .
W 1984 roku wydano grę wideo Bruce Lee .
Życiorys .
Wczesne lata .
Urodził się w San Francisco 27 listopada 1940 jako syn Grace Ho ( 何愛瑜 ; 1907–1996 ) , śpiewaczki , i Lee Hoi-chuena , aktora opery kantońskiej .
Angielskie imię Bruce wymyśliła dlań niańka , a pseudonim „ Mały smok ” , pod którym jest znany w Azji , nadała mu siostra .
Miał dwie siostry – Anges i Phoebe oraz dwóch braci – starszego Petera ( 1939–2008 ) i młodszego Roberta ( ur .
1948 ) .
Jego ojciec był Chińczykiem Han , a matka miała pochodzenie chińsko-europejskie .
Kiedy Lee miał trzy miesiące , wraz z rodzicami przeprowadził się do Koulun w Hongkongu .
Początki kariery .
Miał około trzech miesięcy , gdy po raz pierwszy pojawił się na ekranie trzymany na rękach przez ojca w dramacie `` Kobieta ze złotej bramy '' ( `` Golden Gate Girl '' , 1941 ) .
W wieku pięciu lat wziął udział w dramacie `` Narodziny ludzkości '' ( `` The Birth of Mankind '' , 1946 ) , a jako 8-latek wystąpił w dramacie `` Fu gui fu yun '' ( 1948 ) .
W 1952 rozpoczął naukę w La Salle College , katolickiej szkole im .
Jana Chrzciciela de la Salle w Hongkongu , gdzie zajęcia prowadzili chińscy nauczyciele .
W wieku 13 lat został pobity przez uliczny gang i od tego czasu zaczął trenować kung-fu pod kierunkiem sifu Yipa Mana ( 1957-1958 ) i Wong Shun-leung ( 1957-1958 ) .
Z powodu bójek , w roku szkolnym 1955/1956 został usunięty ze szkoły i naukę kontynuował w szkole jezuickiej dla chłopców im .
Franciszka Ksawerego – St. Francis Xavier ’ s College .
Rozpoczął też treningi bokserskie i zdobywał medale na turniejach sportowych .
W 1958 został mistrzem Hongkongu w cha-cha .
Mając 18 lat wstąpił do gangu ulicznego o nazwie „ 8 Tygrysów Junction Street ” i często popadał w konflikty z policją , więc rodzice zasugerowali mu wyemigrowanie do Stanów Zjednoczonych .
Powrót do Stanów Zjednoczonych .
Po przeprowadzce zamieszkał w chińskiej dzielnicy San Francisco , gdzie pracował w restauracji należącej do jego krewnego , a także jako nauczyciel tańca .
Następnie wyjechał do Seattle , gdzie w grudniu 1960 ukończył James A. Garfield High School i uczęszczał do Edison Technical School .
Później studiował filozofię na University of Washington .
W 1963 opublikował swoją pierwszą książkę , przedstawiającą chińskie kung-fu .
W 1964 przeniósł się wraz ze swoją szkołą do Oakland , gdzie prowadził kursy kung-fu pod nazwą Jun Fan Gung-Fu Institute .
Wkrótce opracował własny styl Jeet Kune Do .
Współpracował z wieloma znanymi w USA mistrzami sztuk walk , jak np .
Edem Parkerem , ekspertem kenpō karate , oraz Jhoonem Rhee – mistrzem taekwondo .
Lee ponownie otworzył w chińskiej dzielnicy Los Angeles swoją szkołę kung-fu .
Kariera ekranowa .
W 1964 na Międzynarodowych Mistrzostwach Karate w Long Beach poznał stylistę Jaya Sebringa , który przedstawił go swojemu przyjacielowi , producentowi telewizyjnemu Williamowi „ Billowi ” Dozierowi .
W 1965 wygrał casting na rolę Kato , skośnookiego pomocnika superbohatera w serialu ABC `` Zielony Szerszeń '' ( `` The Green Hornet '' , 1966–1967 ) , gdzie jego ekranowym partnerem był Van Williams .
Telewidzów zachwyciło jego kung-fu i dostawał więcej listów niż odtwórca tytułowej roli .
