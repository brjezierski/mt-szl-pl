Brugia
Brugia ( nider .
`` Brugge '' , wym .
; fr .
`` Bruges '' ) – miasto w północno-zachodniej Belgii , ośrodek administracyjny prowincji Flandria Zachodnia .
Z powodu obfitości kanałów w historycznej części miasta nazywane jest flamandzką Wenecją .
Ważny port śródlądowy dostępny dla statków morskich , połączony kanałami z Ostendą i Zeebrugge nad Morzem Północnym oraz z Gandawą .
Duży ośrodek przemysłu : koksownia , nowoczesna stalownia , fabryka wagonów i maszyn rolniczych , tradycyjny wyrób koronek i dywanów , odzieżowy i spożywczy , zakłady radiowo-telewizyjne .
Węzeł kolejowy i drogowy , połączenie autostradą z Ostendą i Brukselą .
Główny ośrodek turystyczny .
Historia .
Początki .
Pierwsze fortyfikacje powstały po podboju przez Juliusza Cezara plemienia Menapii w I wieku przed Chrystusem , które miały chronić wybrzeże przed piratami .
Frankowie przejęli ten obszar od starożytnych Rzymian około IV wieku .
W IX wieku wikingowie najechali te ziemie zmuszając Baldwina I , hrabiego Flandrii do wzmocnienia fortyfikacji .
