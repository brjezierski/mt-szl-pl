Bruksela
Bruksela ( fr .
`` Bruxelles '' , nider .
`` Brussel '' , niem .
`` Brüssel '' ) – stolica Belgii , miasto położone w środkowej części kraju nad rzeką Zenne .
Aglomeracja brukselska ( w sensie geograficznym ) prócz obszaru Regionu Stołecznego Brukseli obejmuje także pewną liczbę gmin flamandzkich .
Dane ogólne .
Bruksela jest siedzibą króla i parlamentu belgijskiego , a ponadto jest siedzibą instytucji Unii Europejskiej , NATO i Euroatomu .
Ważny ośrodek handlowy , bankowo-finansowy i kulturalno-naukowy kraju .
Siedziba dwóch królewskich akademii umiejętności ( walońskiej i flamandzkiej ) , Królewskiej Akademii Sztuki ( zał .
1711 ) , Biblioteki Królewskiej ( zał .
1837 ) ; dwóch uniwersytetów ( `` ULB '' i `` VUB '' ) , konserwatorium i instytutów naukowo-badawczych , teatrów , 30 muzeów .
Historia .
Pierwsze wzmianki o mieście pochodzą z 966 , wtedy była to osada obronna .
Później Bruksela była stolicą krainy historycznej Brabancji .
Od XIII w. miasto rozwijało się jako znaczący i prężny ośrodek tkactwa oraz ważny przystanek na szlaku handlowym z Brugii do Kolonii .
Powstało wówczas dzisiejsze centrum miasta z rynkiem ( `` Grand Place '' ) .
W 1695 miasto poważnie ucierpiało w wyniku najazdu króla Francji , Ludwika XIV .
Austriacki minister pełnomocny w 1769 założył w Brukseli Towarzystwo Literackie ( `` Literarische Gesellschaft '' ) , które było zaczątkiem przyszłej Belgijskiej Akademii Nauk .
W 1830 Bruksela została stolicą niepodległej Belgii .
Rok później król Leopold I nakazał wyburzenie murów obronnych i rozbudowę miasta .
Rozwój ten kontynuował jego następca , Leopold II .
W latach 60 .
XIX w. burmistrz Jules Anspach zapoczątkował wielką „ sanację ” i przebudowę miasta .
