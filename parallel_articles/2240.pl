Koźlarz topolowy
Koźlarz topolowy ( `` Leccinum duriusculum '' ( Schulzer ex Kalchbr . )
Singer ) – gatunek grzybów z rodziny borowikowatych ( `` Boletaceae '' ) .
Grzyb jadalny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Schulzer ex Kalchbr .
Po raz pierwszy takson ten opisali w 1874 r. Schulzer i Kalchbrenner nadając mu nazwę `` Boletus duriusculus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1947 r. Singer , przenosząc go do rodzaju `` Leccinum '' .
