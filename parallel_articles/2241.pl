Włochatka ciemna
Włochatka ciemna ( `` Coriolopsis gallica '' ( Fr . )
Ryvarden ) – gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Coriolopsis , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1821 r. Elias Fries , nadając mu nazwę `` Polyporus gallicus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Leif Ryvarden w 1973 r .
Synonimów naukowych ma około 50 .
Niektóre z nich :
Polską nazwę nadał Władysław Wojewoda w 1999 r. , wcześniej gatunek ten opisywany był przez Stanisława Domańskiego jako brunatka włochata , inne źródła wymieniają też nazwę brunatka kosmata .
Morfologia .
Jednoroczny , do podłoża przyrasta zazwyczaj bokiem , rzadko tylko jest rozpostarty .
