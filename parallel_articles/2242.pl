Brunei
Brunei ( , ) , oficjalnie Państwo Brunei Darussalam ( malajski ) – państwo położone w Azji Południowo-Wschodniej , na północnym wybrzeżu wyspy Borneo .
Brunei graniczy z Malezją .
Ma dostęp do Morza Południowochińskiego .
Etymologia .
Pochodzenie nazwy `` Brunei '' nie jest znane .
Według legendy , po odkryciu Brunei jej późniejszy pierwszy sułtan Muhammad Shah miał zawołać `` Baru nah '' ( pol .
„ to jest to ” lub „ tam ” ) i od tego okrzyku ma wywodzić się nazwa `` Brunei '' .
Geografia .
Brunei leży w północno-zachodniej części wyspy Borneo w Azji Południowo-Wschodniej nad Morzem Południowochińskim i podzielone jest na dwa osobne terytoria graniczące od strony lądu z malajskim stanem Sarawak .
Z powierzchnią 5765 km² Brunei jest jednym z najmniejszych państw na świecie .
Jego granica z Malezją wynosi 266 km a łączna długość linii brzegowej to 161 km .
Brunei leży na terenie nizinnym ( średnia wysokość n.p.m .
to 478 m ) o charakterze płaskiej równiny przechodzącej w teren górski na wschodzie .
Najwyższe wzniesienie Brunei to Pagonprick ( 1850 m n.p.m . ) .
Na wschodnim wybrzeżu rozciągają się bagna , reszta wybrzeża jest piaszczysta .
W Brunei występuje wiele krótkich rzek .
Klimat kraju jest równikowy , wilgotny , charakteryzujący się stałą wysoką temperaturą ok. 23–32 °C , dużą wilgotnością powietrza i obfitymi opadami – od ok. 2500 mm rocznie na wybrzeżu do 7500 mm w głębi lądu W Brunei nie występuje pora sucha .
75 % powierzchni Brunei zajmują lasy równikowe .
Na wybrzeżu występują zarośla mangrowe .
W kraju występuje wiele gatunków małp i ptaków .
