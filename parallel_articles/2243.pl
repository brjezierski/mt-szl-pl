Bruneck
Bruneck ( wł .
`` Brunico '' ) – miejscowość i gmina we Włoszech , w regionie Trydent-Górna Adyga , w prowincji Bolzano .
Liczba mieszkańców gminy wynosiła 15 370 ( dane z roku 2009 ) .
Język niemiecki jest językiem ojczystym dla 83,14 % , włoski dla 14,91 % , a ladyński dla 1,95 % mieszkańców ( 2011 ) .
W mieście działa klub hokeja na lodzie HC Pustertal–Val Pusteria .
Atrakcje turystyczne .
W mieście zachowała się średniowieczna starówka – do najciekawszych miejsc należy ulica Stadtgasse otoczona pięknymi XV i XVI wiecznymi kamienicami .
