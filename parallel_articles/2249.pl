Brusy ( Białoruś )
Brusy ( ; ) – wieś na Białorusi , w obwodzie mińskim , w rejonie miadzielskim , w sielsowiecie Swatki .
Historia .
W czasach zaborów wieś w granicach Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym wieś i leśniczówka leżały w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Miadzioł .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 486 osób , 49 było wyznania rzymskokatolickiego , a 437 prawosławnego .
Jednocześnie wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Było tu 90 budynków mieszkalnych .
W 1931 w 98 domach zamieszkiwały 552 osoby .
Leśniczówkę w 1921 zamieszkiwało 7 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 1 domu zamieszkiwały 4 osoby .
Wierni należeli do parafii rzymskokatolickiej w m. Miadzioł i prawosławnej w m. Uzła Wielka .
Miejscowość podlegała pod Sąd Grodzki w Miadziole i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Miadziole .
Po agresji ZSRR na Polskę w 1939 roku wieś znalazła się w granicach BSRR .
W latach 1941–1944 była pod okupacją niemiecką .
Następnie leżała w BSRR .
Od 1991 roku w Republice Białorusi .
