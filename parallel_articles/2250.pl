Bruszczewo
Bruszczewo – wieś w Polsce położona w województwie wielkopolskim , w powiecie kościańskim , w gminie Śmigiel .
Historia .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako Bruszczewo należała do wsi mniejszych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Bruszczewo należało do okręgu śmigielskiego tego powiatu i stanowiło część majątku Koszanowo ( `` niem .
Kuschen '' ) , który należał wówczas do księżnej Joanny Katarzyny Acerenza Pignatelli .
