Okulary korekcyjne
Okulary korekcyjne – przyrząd optyczny znoszący lub zmniejszający skutki wrodzonej lub nabytej wady wzroku , takiej jak nadwzroczność , krótkowzroczność , astygmatyzm ( niezborność ) , czy starczowzroczność .
Historia .
Nie wiadomo , kto i kiedy wynalazł okulary .
Jedna z legend mówi , że wynalazcą jest angielski filozof Roger Bacon .
O tym , że przez szkło widzi się lepiej , wiedziano już w starożytności .
Liczne przekazy mówią , że rzymski cesarz Neron oglądał walki gladiatorów przez specjalnie wyszlifowany szmaragd .
Pierwsze przyrządy , które można by nazwać okularami pojawiły się już w X wieku u Arabów .
W tym samym czasie Chińczycy poprawiali ostrość wzroku patrząc przez kryształy .
W Europie zaś okulary korekcyjne pojawiły się w XIV wieku .
Były jednak bardzo niedokładne , aż do XVI wieku , kiedy Jan Kepler dokonał przełomowych odkryć w dziedzinie optyki .
Natychmiast stały się symbolem mądrości i bogactwa ze względu na wygórowaną cenę .
