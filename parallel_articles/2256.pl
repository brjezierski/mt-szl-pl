Bryoscyphus marchantiae
Bryoscyphus marchantiae ( Fr . )
Spooner – gatunek grzybów z rodziny tocznikowatych ( `` Helotiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Bryoscyphus , Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w roku 1849 Elias Fries nadając mu nazwę `` Helotium marchantiae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1984 r. Brian Martin Spooner .
Synonimy :
Znane jest występowanie `` Bryoscyphus marchantiae '' tylko w niektórych krajach Europy .
W Polsce podano jego stanowiska w 1992 i 1997 roku na plesze wątrobowca z rodzaju porostnica ( `` Marchantia '' ) .
