Rząd Kolumbii Brytyjskiej .
Rząd Kolumbii Brytyjskiej administruje prowincję zgodnie z zakresem kompetencji rządów prowincjonalnych .
Premier Kolumbii Brytyjskiej .
Premier Kolumbii Brytyjskiej jest szefem rządu prowincjonalnego w tej prowincji .
Posiada on bardzo szerokie uprawnienia pozwalające prowadzić skuteczne rządy , jeśli tylko ma zapewnione poparcie w parlamencie .
Premierem zostaje lider partii zdobywającej większość w parlamencie .
