Kolumbia Brytyjska
Kolumbia Brytyjska ( ang .
`` British Columbia '' ) – prowincja Kanady .
Jej zachodnią granicę wyznacza wybrzeże Pacyfiku .
Od północy graniczy z Jukonem , Terytoriami Północno-Zachodnimi i Alaską .
Na wschodzie z prowincją Alberta , a na południu z amerykańskimi stanami Waszyngton , Idaho i Montana .
Nazwa Kolumbia Brytyjska , odnosząca się wcześniej do brytyjskiej kolonii Kolumbia Brytyjska , została wybrana przez królową Wiktorię w 1858 w związku z nazwą rzeki Kolumbia przecinającej tę kolonię .
Rzeka z kolei wzięła swe imię od nazwy statku amerykańskiego kapitana Roberta Graya `` Columbia '' .
Struktura polityczna .
Gubernator porucznik Kolumbii Brytyjskiej .
Gubernator porucznik jest przedstawicielem głowy państwa w prowincji .
Mianuje go Gubernator Generalny na wniosek premiera Kanady złożony zwyczajowo w uzgodnieniu z premierem prowincji .
Gubernator posiada szerokie uprawnienia , z których korzysta co do zasady za radą premiera prowincji .
W sytuacji kryzysu konstytucyjnego Gubernator ma prawo samodzielnie podjąć wszelkie decyzje niezbędne dla przywrócenia stabilności rządu .
Parlament Kolumbii Brytyjskiej .
W skład wchodzi Gubernator porucznik oraz Zgromadzenie Prowincjonalne Kolumbii Brytyjskiej ( `` Legislative Assembly of British Columbia ) , które '' składa się współcześnie w 87 deputowanych `` Members of Legislative Assembly '' w skrócie `` MLA '' .
Wybierani są oni w 87 jednomandatowych okręgach wyborczych .
Partia zdobywająca większość mandatów tworzy rząd prowincjonalny , a jej przewodniczący zostaje premierem Kolumbii Brytyjskiej .
Druga partia w Zgromadzeniu otrzymuje status Oficjalnej Opozycji .
