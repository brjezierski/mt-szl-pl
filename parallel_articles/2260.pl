Brzechów
Brzechów – wieś w Polsce położona w województwie świętokrzyskim , w powiecie kieleckim , w gminie Daleszyce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
Przez wieś przechodzi niebieski szlak turystyczny z Chęcin do Łagowa .
Toponimia .
Nie są znane dokładne informacje na temat pochodzenia nazwy wsi , ale może ona pochodzić od ( drewniana część strzały ) lub Czasownika brzechać ( dzisiejsze `` szczekać '' )
Historia .
Brzechów był „ wsią kościelną ” , klucza kieleckiego kanoników , położoną na terenie powiatu chęcińskiego .
Wieś leży w dolinie pomiędzy Górą Koski i Góry Sikorzą , przy drodze z Daleszyc do Górna , nad bezimiennym dopływem rzeczki Stokowej , w szerokim zakolu Lubrzanki .
