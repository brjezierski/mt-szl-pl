Modernizacje .
W latach 2009-2010 przebudowano stację Brzeg , wraz z przyległymi odcinkami .
Przebudowa stacji objęła prace na długości ponad 4 km .
Wartość inwestycji wyniosła 154 mln zł .
Zmodernizowaną stację uroczyście otwarto 12 stycznia 2010 roku .
W latach 2010-2012 przeprowadzono kosztem 4,2 mln zł gruntowny remont dworca .
Uroczyste otwarcie przebudowanego dworca odbyło się 9 lutego 2012 .
Stację obsługują pociągi uruchamiane przez PKP Intercity kategorii : InterCity oraz Twoje Linie Kolejowe i przez Polregio kategorii : Regio .
Historia .
Stacja w Brzegu , położona w połowie drogi między Wrocławiem a Opolem , jest jedną z najstarszych stacji w Polsce i leży na najstarszej linii uruchomionej na obecnych ziemiach polskich .
Kolej dotarła do Brzegu w sierpniu 1842 roku .
Dworzec w Brzegu , który powstał w latach 1869-1870 jest wpisany do rejestru zabytków .
