Brzeg ( stacja kolejowa )
Brzeg – dworzec i stacja kolejowa w Brzegu , w województwie opolskim , w Polsce .
Stacja należy do III Paneuropejskiego Korytarza Transportowego , łączącego Drezno – Wrocław – Kraków – Kijów .
