Brzesko ( gmina w województwie szczecińskim )
Brzesko ( od 18 IV 1950 `` Przelewice '' ) – dawna gmina wiejska istniejąca w latach 1945–1950 w woj .
szczecińskim ( dzisiejsze woj .
zachodniopomorskie ) .
Siedzibą władz gminy było Brzesko .
Gmina Brzesko powstała po II wojnie światowej na terenie tzw .
Ziem Odzyskanych ( tzw .
III okręg administracyjny – `` Pomorze Zachodnie '' ) .
28 czerwca 1946 gmina – jako jednostka administracyjna powiatu pyrzyckiego – weszła w skład nowo utworzonego woj .
szczecińskiego .
Według stanu z 1 lipca 1948 gmina składała się z 13 gromad : Brzesko , Gardziec , Jesionowo , Kluki , Kłodzino , Kosin , Letnin , Lucin , Płońsko , Przelewice , Rosiny , Obromino i Mechowo .
18 kwietnia 1950 gminę zniesiono w związku z przeniesieniem siedziby gminy z Brzeska do Przelewic i zmianą nazwy jednostki na `` gmina Przelewice '' .
