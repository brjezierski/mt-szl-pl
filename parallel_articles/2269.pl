Brzezinka ( rejon mostowski )
Brzezinka – dawna kolonia , obecnie uroczysko na Białorusi , w obwodzie grodzieńskim , w rejonie mostowskim .
Historia .
Dawniej kolonia w województwie trockim Rzeczypospolitej Obojga Narodów .
W czasach zaborów w granicach Imperium Rosyjskiego , w guberni grodzieńskiej .
W latach 1921–1939 leżały w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Wołpa .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 25 osób , 21 było wyznania rzymskokatolickiego , 1 prawosławnego a 3 ewangelickiego .
Jednocześnie wszyscy mieszkańcy zadeklarowało polską przynależność narodową .
Były tu 3 budynki mieszkalne .
Miejscowość należała do parafii rzymskokatolickiej w Piaskach .
Podlegała pod Sąd Grodzki w Skidlu i Okręgowy w Grodnie ; właściwy urząd pocztowy mieścił się w Wołpie .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
4 grudnia 1939 włączona do nowo utworzonego obwodu białostockiego .
Od czerwca 1941 roku pod okupacją niemiecką .
22 lipca 1941 r. włączona w skład okręgu białostockiego III Rzeszy .
W 1944 miejscowość została ponownie zajęta przez wojska sowieckie i włączona do obwodu grodzieńskiego Białoruskiej SRR .
Od 1991 wchodzi w skład Białorusi .
