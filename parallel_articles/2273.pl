Brzeźnicka Węgorza
Brzeźnicka Węgorza – rzeka na Pojezierzu Zachodniopomorskim , w woj .
zachodniopomorskim , prawobrzeżny dopływ rzeki Reskiej Węgorzy .
Nazwa .
Nazwę `` Brzeźnicka Węgorza '' wprowadzono urzędowo w 1948 roku , zastępując poprzednią niemiecką nazwę `` Aal Bach '' .
Przebieg .
Wypływa z jeziora Studnica na Równinie Drawskiej , z obszaru zamkniętego Poligonu Drawskiego .
Przepływa przez Jezioro Ostrowickie oraz Bucierz , Jezioro Czaple oraz Brzeźno .
Górny bieg rzeki do jeziora Kocioł ma nazwę `` Ostrowitnica '' .
Przyroda .
Rzeka bardzo różni się na poszczególnych odcinkach swego biegu .
W górnym przypomina potok górski , ma szybki i rwący nurt , kamieniste dno i duży spadek .
Dolny nurt ( poniżej Jeziora Żabickiego ) charakteryzuje się szeroką doliną a powolnym spływem mas wody a wzdłuż rzeki występują tam bardzo bogate florystycznie łęgi jesionowo-olszowe , bagienne olsy , żyzne i kwaśne buczyny oraz grądy .
Pospolitym legowiskiem ptaków jest końcowy odcinek nurtu , u ujścia do Regi .
W czasie swego spływu tworzy liczne małe wodospady .
Nad Brzeźnicką Węgorzą od listopada 2007 znajduje się specjalny obszar ochrony siedlisk ( obszar chroniony w ramach programu Natura 2000 ) o powierzchni 592,2 ha .
Brzegi rzeki są miejscem bytowania orlika krzykliwego oraz wielu gatunków kaczek .
Wzdłuż linii brzegowej Brzeźnickiej Węgorzy rosną zbiorowiska szuwarowe , stanowiące siedlisko bielika , trzmielojada , kani rudej i czarnej , puchacza , żurawia , bociana czarnego , derkacz , dzięcioła czarnego , skowronka borowego , a także bobrów i wyder .
O dużej czystości rzeki świadczy występowanie słodkowodnego krasnorosta ( `` Hildebrandtia rivularis '' ) .
Turystyka .
Dnem doliny rzeki prowadzi rowerowy szlak turystyczny `` Rzeka Brzeźnicka Węgorza – Wodospady '' , przecinający w kilku miejscach nurt .
Szlak jest oznakowany kolorem czarnym , ma postać pętli rozpoczynającej się i kończącej w Drawsku Pomorskim i długość 40 kilometrów .
Brzeźnicka Węgorza jest miejscem częstych spływów kajakowych , a dzięki połączeniu z Regą można z niej wypłynąć aż na Morze Bałtyckie .
Szlak jednak należy do bardzo trudnych z powodu wielu przeszkód ( ruiny młynów Kołatka i Białego Młyna , wodospady , kamieniste dno , szybki nurt , duże spadki o charakterze wód górskich ) .
Stan czystości .
W 2008 r. przeprowadzono badania jakości wód Brzeźnickiej Węgorzy w punkcie ujścia do Reskiej Węgorzy .
W ich wyniku oceniono elementy fizykochemiczne na II klasy , elementy biologiczne określono na I klasy , a stan ekologiczny na dobry .
W ogólnej dwustopniowej ocenie stwierdzono dobry stan wód Brzeźnickiej Węgorzy .
