Brzeżawa
Brzeżawa – wieś w Polsce położona w województwie podkarpackim , w powiecie przemyskim , w gminie Bircza .
Leży na Pogórzu Przemyskim .
W latach 1975–1998 miejscowość administracyjnie należała do województwa przemyskiego .
Historia .
Wieś powstała na początku XV wieku .
Pierwsza wzmianka pochodzi z 1464 roku , kiedy to Brzeżawa należała do majątku birczańskiego , nosząc nazwę `` Twardego Wolya '' .
Wieś prawa wołoskiego , położona była na początku XVI wieku w ziemi sanockiej województwa ruskiego .
Spis z 1510 roku wykazał podatki płacone z : 10 łanów , młyna , karczmy , popa i 200 owiec .
