Brzostek
Brzostek – miasto w Polsce , w województwie podkarpackim , w powiecie dębickim , siedziba gminy miejsko-wiejskiej Brzostek .
Miejscowość leży nad rzeką Wisłoką .
Obszar Brzostku zajmuje 8,76 km² , w 2021 roku zamieszkiwało go 2 716 osób .
W mieście znajduje się przedszkole , szkoła podstawowa , jednostka Ochotniczej Straży Pożarnej , a także kilka niewielkich zakładów przemysłowych – stanowi ono centrum usługowo-handlowe dla okolicznych wsi .
Brzostek pierwotnie znajdował się w rękach benedyktynów z Tyńca .
