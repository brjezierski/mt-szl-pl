Brzóski-Falki
Brzóski-Falki – wieś w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
Zaścianek szlachecki Falki należący do okolicy zaściankowej Brzoski położony był w drugiej połowie XVII wieku w powiecie brańskim ziemi bielskiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Apostołów Piotra i Pawła w Wysokiem Mazowieckiem .
Historia .
W pobliżu położonych jest kilka innych wsi o nazwie Brzóski , różniących się drugim członem nazwy .
W XIX w. wsie tworzyły okolicę szlachecką `` Brzóski '' .
