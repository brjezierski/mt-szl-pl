Brzóski-Tatary
Brzóski-Tatary – wieś w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
Zaścianek szlachecki Tatary należący do okolicy zaściankowej Brzoski położony był w drugiej połowie XVII wieku w powiecie brańskim ziemi bielskiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Apostołów Piotra i Pawła w Wysokiem Mazowieckiem .
Historia .
Miejscowość wzmiankowana w roku 1624 .
`` Brzóski Tatary '' wymienione w spisie podatkowym z 1676 r .
