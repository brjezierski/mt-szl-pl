Bucaramanga
Bucaramanga – miasto w północnej Kolumbii , położone na zachodnim stoku Kordyliery Wschodniej ( Andy Północne ) .
Położone jest na wysokości 959 metrów .
Miasto jest ośrodkiem administracyjnym departamentu Santander .
Według spisu ludności z 30 czerwca 2018 roku miasto liczyło 570 752 mieszkańców .
W mieście działa przemysł petrochemiczny , chemiczny i cementowy .
Ponadto rozwinięte jest rzemiosło artystyczne .
Bucaramanga jest ośrodkiem handlowym i usługowym regionu rolniczego .
W mieście znajdują się dwa uniwersytety ( założone w 1947 i 1955 roku ) .
W pobliżu funkcjonuje międzynarodowy port lotniczy Palonegro .
Ponadto w mieście znajduje się siedziba rzymskokatolickiej archidiecezji Bucaramanga .
