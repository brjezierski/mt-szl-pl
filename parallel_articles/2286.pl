Księgarnia
Księgarnia – przedsiębiorstwo handlowe zajmujące się detaliczną , a czasami hurtową sprzedażą książek , a także innych wydawnictw i artykułów pokrewnych takich jak czasopisma , mapy , czy przewodniki .
A także jednostka operatywno-wykonawcza wchodząca w skład przedsiębiorstwa księgarskiego , będąca stałym punktem detalicznej sprzedaży książek i innych wydawnictw , dysponująca lokalem wyposażonym w odpowiednia urządzenia służące do ekspozycji i sprzedaży .
Rodzaje księgarń :
Księgarnia nakładowa to księgarnia zajmująca się rozprowadzaniem książek wydanych własnym nakładem .
