Buchtar Tabuni
Buchtar Tabuni ( ur .
1979 ) – papuaski aktywista , działacz na rzecz niepodległości Papui Zachodniej .
Urodził się w niewielkiej wiosce Papani .
Od 1998 studiował inżynierię w Makasarze .
W działalność polityczną zaangażował się po przypadkowej śmierci swojego dalekiego krewnego Opinusa Tabuniego podczas rozpędzania pokojowej demonstracji w Wamena ( 9 sierpnia 2008 ) .
Był jednym z założycieli Komitetu Narodowego Papui Zachodniej ( Komite Nasional Papua Barat , KNPB ) .
Organizował akcje protestacyjne wyrażające wsparcie dla kampanii International Parlamentarians for West Papua .
3 grudnia 2008 został aresztowany , skazano go na trzy lata pozbawienia wolności .
Po mających miejsce w grudniu 2010 zamieszkach w zakładzie karnym w Abepurze został oskarżony o przyczynienie się do ich wywołania .
17 sierpnia 2011 został zwolniony z więzienia .
