Złociec siarkowy
Złociec siarkowy , złotak siarkowy ( `` Buchwaldoboletus hemichrysus '' ( Berk .
& amp ; M.A .
Curtis ) Pilát ) – gatunek grzybów z rodziny borowikowatych ( `` Boletaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Buchwaldoboletus , Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy takson ten opisali w 1873 r. Miles Joseph Berkeley i Moses Ashley Curtis nadając mu nazwę `` Boletus hemichrysus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1969 r. Albert Pilát , przenosząc go do rodzaju `` Buchwaldoboletus '' .
Synonimy :
W 1960 r. Alina Skirgiełło nadała nazwę polską złotak siarkowy dla naukowej nazwy `` Pulveroboletus hemichrysus '' .
Po przeniesieniu gatunku do rodzaju `` Buchwaldoboletus '' stała się ona niespójna z nową nazwą naukową .
Z tego powodu w 2021 r. Komisja ds .
Polskiego Nazewnictwa Grzybów przy Polskim Towarzystwie Mykologicznym zarekomendowała nową nazwę – złociec siarkowy .
Morfologia .
Średnica do 6 cm , szeroko wypukły .
Powierzchnia nieco chropowata , jasnożółta , żółtawo oprószona .
Brzeg wąsko sterylny .
Zbiegające na trzon , czerwonawo-pomarańczowe , ciemniejsze przy brzegu .
