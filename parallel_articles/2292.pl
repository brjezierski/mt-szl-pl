Buczacz
Buczacz ( ukr .
Бучач ) – miasto w rejonie czortkowskim obwodu tarnopolskiego na Ukrainie , nad rzeką Strypą .
Siedziba rejonu buczackiego w latach 1940–1941 oraz 1944–2020 .
Ośrodek przemysłu metalowego , spożywczego i paszowego .
Leży w pobliżu drogi krajowej N18 .
Przez miasto przebiega droga terytorialna T2001 .
W mieście znajduje się stacja kolejowa .
Prywatne miasto szlacheckie lokowane w 1379 roku położone było w XVI wieku w województwie ruskim .
