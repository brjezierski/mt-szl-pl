Budzanów
Budzanów ( ukr .
Буданів , `` Budaniw '' , ros .
Буданов , `` Budanow '' ) – wieś na Ukrainie , w rejonie czortkowskim obwodu tarnopolskiego , w hromadzie Białobożnica , leżąca nad rzeką Seret w czworoboku pomiędzy Czortkowem , Buczaczem , Trembowlą a Kopyczyńcami .
Ludność : 1634 osoby ( 2005 ) .
Historia .
W 1549 r. król Polski Zygmunt II August nadał miejscowości magdeburskie prawa miejskie .
Miasto w dawnym województwie ruskim Rzeczypospolitej założył Jakub Budzanowski .
W XIX w. posiadała prawa miejskie w powiecie czortkowskim , i była zamieszkana przez 4661 osób ( rzymscy katolicy 1 100 , greko-katolicy 1 674 , żydzi 1 887 ) .
Ważniejszymi obiektami były : klasztor , szpital , szkoła .
Właścicielem większej posiadłości w drugiej połowie XIX w. był Władysław hr .
Baworowski .
Jego syn Jerzy był posłem .
Podczas okupacji pozbawiony praw miejskich i włączony do nowej wiejskiej gminy Budzanów .
Do 2020 roku część rejonu trembowelskiego , od 2020 – czortkowskiego .
