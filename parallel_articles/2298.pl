Nazwa języka wywodzi się od nazwy wsi Buduch ( inna nazwa : Budad ) , w którym zamieszkuje największa grupa użytkowników języka .
Buduchyjski jest językiem aglutynacyjnym .
Język ten nie wykształcił piśmiennictwa .
Jest używany wyłącznie w sytuacjach nieformalnych , w domu , wśród przyjaciół .
W charakterze języka literackiego używany jest natomiast język azerski .
Ze względu na niewielką liczbę użytkowników języka , a także z powodu obniżania się prestiżu języka wśród jego użytkowników , uważa się , iż buduchyjski jest językiem zagrożonym wymarciem w ciągu najbliższych kilkudziesięciu lat .
