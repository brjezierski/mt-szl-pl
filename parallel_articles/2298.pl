Język buduchyjski
Język buduchyjski ( także : budychyjski , buduchski , nazwa własna Будад мез ) – jeden z niewielkich języków kaukaskich , używany przez Buduchów .
Należy do zespołu samurskiego , podgrupy dagestańskiej w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Wraz z językami kryzyjskim oraz szachdagijskim wydzielany jest czasami w ramach zespołu samurskiego w odrębną podgrupę .
Język ten używany jest przez maksymalnie ok. 5 tys .
