Budziska ( powiat mrągowski )
Budziska ( dawniej Budziszki , niem .
`` Budzisken '' , 1929–1945 `` Wachau '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie mrągowskim , w gminie Mrągowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
Historia .
Wieś lokowana w 1533 r. , ale wcześniej w tym miejscu ( w 1437 r. ) istniała jakaś osada , która później ( zapewne na skutek zniszczeń wojny trzynastoletniej ) została opuszczona .
W tamtym czasie takie opuszczone osady nazywano `` Budami '' lub `` Budziskami '' , można więc sądzić , że nazwa wsi od takiej nazwy się wzięła .
W 1533 r. 5 łanów sołeckich ( po 27 grzywien od łanu ) kupił niejaki `` Zese Schwechowski '' ( Czesio Szwejkowski ) , od starosty ryński `` Georga von Diebesa '' , celem założenia wsi czynszowej na 50 łanach ( 853 ha ) .
W późniejszym czasie zaledwie trzecia część jej areału była w użytkowaniu chłopów , resztę należała do folwarku .
