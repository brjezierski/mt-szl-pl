Bukareszt
Bukareszt ( ) ( Miasto Bukareszt , `` Municipiul București '' ) – stolica i największe miasto Rumunii oraz centrum przemysłowo-usługowe położone w południowo-wschodniej części kraju , nad rzeką Dymbowicą .
Pierwsze wzmianki o mieście pochodzą z roku 1459 , status stolicy kraju Bukareszt uzyskał w 1862 r. Przez XIX i XX wiek przeszedł wiele zmian i stał się centrum kulturalnym i medialnym Rumunii .
Eklektyczna architektura , która stanowi mieszankę zabudowy historycznej , socrealistycznej i nowoczesnej , odzwierciedla historię miasta .
W okresie międzywojennym Bukareszt uzyskał przydomek `` Małego Paryża '' ( `` Micul Paris '' ) .
Duża część śródmieścia została zniszczona podczas działań wojennych , trzęsień ziemi i wprowadzania w życie programu systematyzującego Nicolae Ceaușescu .
Obecnie miasto przeżywa rozwój ekonomiczny i gospodarczy .
Pod względem administracyjnym , miasto ma status równy okręgowi .
Dzieli się na sześć sektorów .
Jest również siedzibą władz administracyjnych okręgu Ilfov .
