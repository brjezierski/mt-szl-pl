Geografia .
Miasto Bukareszt leży na obu brzegach rzeki Dâmbovița , która wpływa do rzeki Argeș , dopływu Dunaju .
W obrębie miasta znajduje się również kilka jezior , z których największe to Floreasca , Tei i Colentina .
Leżą one wzdłuż Colentiny , dopływu Dâmbovițy .
Dodatkowo , w centrum miasta znajduje się małe sztuczne jezioro , Cișmigiu .
Jest ono otoczone przez Ogrody Cișmigiu , otwarte w 1847 według planów architekta niemieckiego , Carla Friedricha Wilhelma Meyera .
Ogrody mają długą historię , w której często przewijają się znani pisarze i poeci .
Oprócz Cișmigiu , Bukareszt posiada jeszcze dwa inne duże parki : Herăstrău i ogród botaniczny .
Herăstrău to duży park w północnej części miasta .
Ogród botaniczny należy do największych na świecie – występuje w nim ponad 10 000 gatunków roślin , w tym wiele egzotycznych .
Bukareszt leży w południowo-wschodniej części Równiny Wołoskiej , w miejscu dawniej porośniętym lasem Vlăsiei – obecnie w jego miejscu znajdują się osiedla mieszkaniowe .
Bukareszt leży na siedmiu wzgórzach , co nawiązuje do tradycji rzymskiej .
