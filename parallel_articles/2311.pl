Bukowna ( Karkonosze )
Bukowna ( 542 m n.p.m . )
– szczyt w Karkonoszach w obrębie Pogórza Karkonoskiego .
Położony jest w północno-wschodniej części Pogórza Karkonoskiego , między Podgórzynem , Przesieką a Sosnówką .
Leży w grzbiecie biegnącym od Młynarza poprzez Wierzchnicę , Warzelnię , Bukowną , Lisiurę i kończącym się Podgórzynką .
Zbudowany z granitu karkonoskiego .
Na zboczach liczne skałki .
Porośnięty lasem regla dolnego .
