Bunia
Bunia – miasto w Demokratycznej Republice Konga , stolica prowincji Ituri , na północ od góry Hoyo i na zachód od Jeziora Alberta .
Miasto stało się teatrem walk pomiędzy grupami etnicznymi Lendu i Hema .
W celu zaprowadzenia porządku , do miasta skierowano siły międzynarodowe ( Operacja Artemida ) .
