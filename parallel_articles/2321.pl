Burabaj ( kaz .
i ros .
: Бурабай ) – kompleks skoczni narciarskich położonych w kazachskim Szczuczyńsku , w których skład wchodzą skocznie o rozmiarach K125 ( HS-140 ) , K90 ( HS-99 ) , K65 oraz trzy mniejsze .
Kompleks został wybudowany w 2018 roku .
Najdalszy skok na skoczni dużej oddał Słoweniec Jurij Tepeš , który skoczywszy 154 metry nie ustał swojej próby .
Oficjalny rekord skoczni normalnej należy do Rosjanina Danila Sadreeva i wynosi 99,5m .
