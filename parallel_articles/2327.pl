Burdż Chalifa
Burdż Chalifa ( arab .
برج خليفة , `` Burǧ Ḫalīfa '' , `` Burj Khalifa '' , po polsku także : `` Wieża Chalify '' , przed otwarciem : `` Burdż Dubajj '' , arab .
برج دبي , `` Burǧ Dubayy '' , `` Burj Dubai '' , pol .
`` Wieża Dubaju '' ) – wieżowiec w Dubaju , w Zjednoczonych Emiratach Arabskich , zbudowany przez przedsiębiorstwa Samsung Constructions , BESIX i Arabtec , o wysokości 828 metrów .
Najwyższy budynek świata , który pobił rekord wysokości swojego poprzednika - Masztu radiowego w Konstantynowie ( 646m ) , polskiej budowli będącej najwyższa na świecie w latach 1974-1991 ( do momentu zawalenia ) .
Jego nazwa pochodzi od imienia szejka Chalify ibn Zajida Al Nahajjana , byłego prezydenta Zjednoczonych Emiratów Arabskich .
Budowa , rozpoczęta 21 września 2004 , zakończyła się 16 sierpnia 2009 .
Wysokość 827,9 metrów została osiągnięta 17 stycznia 2009 , a oficjalne otwarcie nastąpiło 4 stycznia 2010 .
Budynek ma 163 piętra użytkowe .
Koszt jego budowy wyniósł 1,5 miliarda dolarów .
Wygląd i wystrój .
Wieżowiec Burdż Chalifa zaprojektowany został przez przedsiębiorstwo architektoniczne Skidmore , Owings and Merrill , które projektowało także budynki Willis Tower oraz 1 World Trade Center .
Ogólny jego wygląd nawiązuje do kwiatu pustyni z rodzaju `` Hymenocallis '' oraz architektury islamu ( różne ornamenty ) .
Budowla składa się z centralnego rdzenia oraz trzech „ ramion ” , które w miarę zwiększania się wysokości są coraz mniejsze , co nadaje jej smukłość .
Na samym szczycie centralny rdzeń przechodzi w iglicę .
Najniższe piętra przeznaczono na hotel , którego wystrojem zajął się Giorgio Armani .
