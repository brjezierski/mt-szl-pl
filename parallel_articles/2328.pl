Język buriacki
Język buriacki ( буряад хэлэн , `` buriaad helen '' ) – język Buriatów , należący do podrodziny języków mongolskich .
Większość użytkowników języka zamieszkuje w autonomicznej republice Buriacji na terenie Federacji Rosyjskiej .
Oprócz odmiany używanej w Rosji istnieją również odmiany języka używane w Chinach i Mongolii .
Większość użytkowników języka buriackiego w Rosji posługuje się także językiem rosyjskim , który ma w kraju status urzędowego .
Bywa opisywany jako dialekt języka mongolskiego .
Nie jest jednak dobrze wzajemnie zrozumiały z chałcha-mongolskim , służącym jako język urzędowy niepodległej Mongolii .
