Historia .
W 1703 Buriację włączono do Państwa Moskiewskiego na mocy układu z cesarzem Piotrem I. W latach 1891–1916 przez terytorium Buriacji poprowadzono Kolej Transsyberyjską .
W latach 1921–1922 powstały dwa Buriacko-Mongolskie Obwody Autonomiczne ( jeden w Rosyjskiej FSRR , drugi w Republice Dalekiego Wschodu ) , które 30 maja 1923 zostały połączone w Buriat-Mongolską Autonomiczną Socjalistyczną Republikę Radziecką .
W 1958 nazwę zmieniono na Buriacką ASRR , a w 1992 powstała Republika Buriacka w składzie Federacji Rosyjskiej .
Podział administracyjny .
Obszar Buriacji podzielony jest na 21 rejonów i dwa miasta wydzielone .
Tablice rejestracyjne .
Tablice pojazdów zarejestrowanych w Buriacji mają oznaczenie 03 w prawym górnym rogu nad flagą Rosji i literami RUS .
