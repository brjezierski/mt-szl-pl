Buriacja
Buriacja ( , ) – republika autonomiczna wchodząca w skład Federacji Rosyjskiej .
Buriacja wchodzi w skład Dalekowschodniego Okręgu Federalnego , znajduje się w Syberii Wschodniej ( Zabajkale ) .
Od południa graniczy z Mongolią , od zachodu z obwodem irkuckim , a od wschodu z Krajem Zabajkalskim .
Najważniejsze miasta to Ułan Ude , Babuszkin , Gusinooziorsk , Zakamiensk , Kiachta i Siewierobajkalsk .
Geografia .
Kraj jest w przeważającej części górzysty .
Najwyższym szczytem jest Munku-Sardyk ( 3491 m n.p.m . )
w paśmie Sajanu Wschodniego .
Na terytorium Buriacji znajduje się większa część ( 60 % linii brzegowej ) jeziora Bajkał .
Najważniejsze rzeki to Witim ( dopływ Leny ) i Selenga .
Strefa czasowa .
Buriacja należy do irkuckiej strefy czasowej ( IRKT ) .
UTC +8:00 przez cały rok .
Wcześniej , przed 27 marca 2011 roku , obowiązywał czas standardowy ( zimowy ) strefy UTC+8:00 , a czas letni – UTC+9:00 .
