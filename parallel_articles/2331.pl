Burkina Faso
Burkina Faso – państwo w Afryce Zachodniej bez dostępu do morza .
Dawniej Górna Wolta ( `` Republique du Haute-Volta '' ) , w 1984 prezydent Thomas Sankara nadał mu nową nazwę , która oznacza „ kraj prawych ludzi ” w językach mossi i diula , będących głównymi rdzennymi językami kraju .
Stolicą kraju jest Wagadugu ( Ouagadougou ) , nazywane przez Burkińczyków `` Ouaga '' .
Członek Unii Afrykańskiej i ECOWAS .
Geografia .
Obszar kraju jest przeważnie równinny , lokalnie z terenami wzniesionymi , zwłaszcza w zachodniej części kraju , gdzie sięga Wyżyna Gwinejska i znajduje się tu najwyższy szczyt kraju Pic de Nakourou ( Tena Kuru ) – 749 m n.p.m .
przy samej granicy z Mali .
Nieco dalej na wschód rozciąga się pasmo Chaîne de Banfora z główną atrakcją turystyczną kraju – wodospadami Cascades de Karfiguéla .
Na terenie Burkina Faso rozpoczynają swój bieg także trzy ważne rzeki regionu : Wolta Czarna , Wolta Czerwona i Wolta Biała , które zlewają się następnie , tworząc rzekę Wolta .
Naturalną szatą roślinną państwa jest sawanna .
Na południowym zachodzie przy granicy z Mali oraz Wybrzeżem Kości Słoniowej występuje bujna roślinność sawannowa .
Na północy natomiast rozciąga się sawanna sucha z niskimi akacjami i kolczastymi krzewami .
Na północnym wschodzie kraju występuje roślinność pustynna .
Często spotykanymi tu przedstawicielami świata zwierząt są żyrafy , słonie , lwy , zebry i sępy .
Klimat .
W Burkinie Faso występuje głównie klimat tropikalny z dwiema odrębnymi porami roku .
Pora deszczowa występuje na południu kraju od maja do października , a na północy jest krótsza i trwa od lipca do września .
Pora sucha trwa od 8 do 9 miesięcy , od października do czerwca .
Podczas pory deszczowej opady w kraju utrzymują się na poziomie między 600 a 900 mm ( 24-35 cali ) .
W suchej porze wieje pylny i gorący wiatr pasatowy harmattan .
Burkina Faso jest często nawiedzana przez długotrwałe susze .
