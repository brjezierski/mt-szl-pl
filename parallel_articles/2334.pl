Burmistrz ( Polska )
Burmistrz – jednoosobowy organ wykonawczy ( wójt ) gminy miejskiej i gminy miejsko-wiejskiej .
W miastach na prawach powiatu , powyżej 100 000 mieszkańców lub posiadających historycznie przyjęte nazewnictwo , organem wykonawczym jest prezydent miasta .
Burmistrz jest również kierownikiem urzędu miasta ( lub gminy miejsko-wiejskiej ) .
Z mocy ustawy jest też kierownikiem urzędu stanu cywilnego , lecz może w swoje miejsce mianować odrębnego kierownika tego urzędu , co też z reguły jest praktykowane .
Istnieje możliwość odwołania burmistrza przez wyborców .
Do burmistrza stosuje się przepisy o wójcie i jego wyborze .
Burmistrz istniał w okresie II Rzeczypospolitej i Polski Ludowej do 1950 r .
Historia .
W Polsce urząd ten pojawił się w XIII wieku , razem z rozwojem samorządu miejskiego .
Burmistrz był przewodniczącym rady miejskiej , wybieranym zwykle co kwartał spośród rajców .
W dawnym , niemieckim , prawie miejskim burmistrz był jednocześnie przewodniczącym organu stanowiącego , czyli rady i jednocześnie organem wykonawczym miasta .
W XIX wieku nastąpiło oddzielenie funkcji burmistrza od funkcji przewodniczącego rady miejskiej .
Od tej pory przewodniczył on kolegialnemu organowi wykonawczemu miasta , zwanemu magistratem lub zarządem miejskim .
Niedługo po II wojnie światowej w Polsce w 1950 roku urząd burmistrza zniesiono , razem z całym samorządem terytorialnym .
Kompetencje przejęło prezydium Miejskiej Rady Narodowej , a później , po reformie w 1973 roku , naczelnik miasta .
Urząd burmistrza ponownie ustanowiono po przywróceniu samorządu terytorialnego w 1990 roku .
Był wybierany przez radę miasta wspólnie z pozostałymi członkami zarządu miasta .
Od 2002 r. burmistrz jest wybierany przez ogół mieszkańców gminy w bezpośrednich wyborach powszechnych i jest jednocześnie jednoosobowym organem wykonawczym gminy ( likwidacja zarządu miasta ) .
