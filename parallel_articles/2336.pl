Burslem
Burslem – miasto w Anglii , w hrabstwie ceremonialnym Staffordshire , w dystrykcie ( unitary authority ) Stoke-on-Trent .
Leży 27 km na północ od miasta Stafford i 221 km na północny zachód od Londynu .
Miasto liczy 14 303 mieszkańców .
Burslem jest wspomniana w Domesday Book ( 1086 ) jako `` Bacardeslim '' .
24 grudnia 1945 roku urodził się tu Lemmy Kilmister .
