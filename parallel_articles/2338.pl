Bury St Edmunds
Bury St Edmunds – miasto i civil parish w Anglii , w Suffolk , w dystrykcie West Suffolk .
W 2011 civil parish liczyła 40664 mieszkańców .
Przemysł browarniczy i cukrowniczy .
W centrum miasta znajdują się ruiny opactwa , gdzie został pogrzebany król Edmund , władca Anglii Wschodniej , zabity przez wikingów w 869 roku .
Opactwo do śmierci Tomasza Becketa było najczęstszym celem pielgrzymek w Anglii .
