Busseto ( stacja kolejowa )
Busseto ( włoski : `` Stazione di Busseto '' ) – stacja kolejowa w Busseto , w regionie Emilia-Romania , we Włoszech .
Znajdują się tu 2 perony .
Położona jest na linii Cremona-Fidenza .
Stacja posiada dwupiętrowy budynek .
Usług dla podróżnych są nieobecne z powodu niskiej frekwencji .
Stacja posiada dwa perony , połączone przejściem , drugi jest stosowany w przypadku mijanki .
