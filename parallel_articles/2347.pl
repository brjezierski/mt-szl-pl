Busto Arsizio ( stacja kolejowa )
Busto Arsizio ( wł : `` Stazione di Busto Arsizio '' ) – stacja kolejowa w Busto Arsizio , w regionie Lombardia , we Włoszech .
Znajdują się tu 2 perony .
Obecna stacja znajduje się w Piazza Volontari della Libertà , wzdłuż Strada Garottola do Olgiate Olona .
Historia .
Do początku XX wieku stara stacja była zarządzana przez `` Società per le Strade Ferrate del Mediterraneo '' , znajdując się na skrzyżowaniu obecnych `` Viale Duca d'Aosta '' i `` Via XX Settembre '' , gdzie znajdowały się perony .
Uruchomiono linię z Busto Arsizio do Gallarate i Novary , z odcinkiem Mediolan-Rho pochodzi z 20 grudnia 1860 .
Nowy dworzec kolejowy , imponujący budynek w porównaniu do starego , powstał w 1905 r. i otwarty został przez Benito Mussoliniego 26 października 1924 .
W 2004 otwarto tunel dla pieszych na Via Palermo służący dla osób dojeżdżających do pracy .
