Butare
Butare ( do 1962 `` Astrida '' ) – miasto w południowej Rwandzie , w Prowincji Południowej , stolica dystryktu Huye .
50 220 mieszkańców ( 2012 ) .
Czwarte pod względem liczby mieszkańców miasto kraju .
W Butare działał Narodowy Uniwersytet Rwandy , założony w 1963 roku .
Miasto jest siedzibą katolickiej diecezji .
