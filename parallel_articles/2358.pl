Buzu Squat
Buzu Squat – polski zespół muzyczny .
Jego dorobek opiera się na wielu gatunkach , głównie jest to pop-rock .
Historia .
Buzu Squat został założony w 1992 roku w Warszawie .
Zespół wydał w 1993 jedyną w historii zespołu płytę długogrającą – `` Dowódcy miłości '' w nakładzie 40 tys .
egzemplarzy , z której dwa utwory : „ Przebudzenie ” i „ Kocie sprawy ” weszły na stałe do kanonu polskiego pop-rocka .
Rok później zespół wydał mini-płytę zawierającą tylko dwa utwory .
W 2002 wystąpili w Żarach na festiwalu Przystanek Woodstock , na podstawie którego powstał film dokumentalny `` Przystanek Woodstock - Najgłośniejszy Film Polski '' w reżyserii Jerzego Owsiaka .
W tym samym roku zespół nagrał kolejny singiel – „ Milion dolarów ” .
W 2010 zespół po wielu latach nagrał , wspólnie z załogą żaglowca Wielki Błękit , nowy singiel zatytułowany „ Nadciąga wielki błękit ” .
