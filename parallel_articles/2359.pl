Bućki
Bućki – wieś w Polsce położona w województwie podlaskim , w powiecie suwalskim , w gminie Przerośl .
Wieś królewska starostwa niegrodowego przeroskiego położona była w końcu XVIII wieku w powiecie grodzieńskim województwa trockiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
Przez miejscowość przepływa rzeka Błędzianka .
