Język bułgarski
Język bułgarski ( bułg .
, [ ] ) – język z grupy języków południowosłowiańskich oraz bałkańskiej ligi językowej .
Posługuje się nim ok. 9 mln ludzi , głównie w Bułgarii , gdzie jest językiem urzędowym , ale także w Grecji , Kanadzie , Macedonii Północnej , Mołdawii , Rumunii , Serbii , USA , Turcji i na Ukrainie .
Język bułgarski wywodzi się z języka Słowian bałkańskich , którzy przybyli na tereny Półwyspu Bałkańskiego między V w. a VII w. We współczesnym języku bułgarskim pozostały ślady mowy Protobułgarów z czasów , gdy Słowianie bałkańscy tworzyli z nimi wspólne państwo ( VII w. – X w. ) .
