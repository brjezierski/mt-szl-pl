Bułgaria
Bułgaria , Republika Bułgarii ( ) – państwo położone w południowo-wschodniej Europie , na Bałkanach .
Graniczy z Serbią oraz Macedonią Północną od zachodu , Grecją i Turcją od południa , Morzem Czarnym od wschodu i Rumunią od północy .
Bułgaria jest członkiem : Organizacji Narodów Zjednoczonych ( od 1955 ) , Organizacji Traktatu Północnoatlantyckiego – NATO ( od 2004 ) oraz Unii Europejskiej ( od 2007 ) .
Geografia .
Bułgaria leży w południowej Europie nad Morzem Czarnym , w północno-wschodniej części Półwyspu Bałkańskiego .
Od północy graniczy z Rumunią , od zachodu z Serbią i Macedonią Północną od południa z Grecją i Turcją .
Stolicą Bułgarii jest Sofia ( 1,1 mln mieszkańców ) , inne większe miasta to Płowdiw , Warna , Burgas , Ruse , Stara Zagora , Plewen i Wraca .
Warunki naturalne .
Około 60 % powierzchni Bułgarii zajmują tereny wyżynne i górskie , średnie wzniesienie kraju wynosi ok. 470 m n.p.m .
Wzdłuż północnej granicy rozciąga się Nizina Naddunajska , zajmująca całą północną część kraju .
W środkowej części Bułgarii leży jej główny masyw górski – Stara Płanina ( Bałkan ) wraz z pasem Kotlin Zabałkańskich ( Sofijska , Kazanłycka , Sliweńska i in . ) .
