W Busku rzeka Bug łączy się z rzeką Pełtwią , ze względu na dużą liczbę odnóg Bugu i mostów miasto było nazywane `` galicyjską Wenecją '' .
Miasto królewskie lokowane w 1411 roku położone było w XVI wieku w województwie bełskim .
Busk wchodził w skład starostwa grodowego buskiego na początku XVIII wieku .
