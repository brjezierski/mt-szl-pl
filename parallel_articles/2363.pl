Bużumbura
Bużumbura ( ) – miasto w Burundi , do 23 grudnia 2018 stolica tegoż państwa ( obecnie siedziba władz ) , główny port położony na północno-wschodnim rogu jeziora Tanganika .
Według danych szacunkowych z 2020 roku liczy ponad 1 mln mieszkańców i jest największą aglomeracją , centrum administracyjnym , komunikacyjnym i ekonomicznym Burundi .
Na północ od miasta znajduje się międzynarodowy port lotniczy Bużumbura .
Sieć dróg łączy Bużumburę z miastami Bukavu i Goma w Demokratycznej Republice Konga oraz z Kigali w Rwandzie .
Historia .
Bużumbura rozrosła się z małej wioski po tym , kiedy stała się posterunkiem wojskowym w Niemieckiej Afryce Wschodniej w 1889 .
W okresie belgijskiej władzy kolonialnej była ośrodkiem administracyjnym terytorium Ruanda-Urundi .
W 1962 roku została stolicą niepodległego Burundi , którą pozostawała do 2018 roku .
Nazwa miasta zmieniła się z Usumbura na Bużumbura , kiedy Burundi zdobyło niepodległość w 1962 .
Gospodarka .
Produkuje się tu cement , mydło i tekstylia .
Bużumbura jest także głównym portem Burundi a statkami eksportuje się kawę , skóry i rudę cyny .
Edukacja .
W mieście jest Université du Burundi .
