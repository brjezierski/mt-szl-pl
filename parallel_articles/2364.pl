Bużyska
Bużyska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Korczew .
Leży nad Bugiem .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Stanisława Biskupa Męczennika i św .
Anny w Knychówku .
Historia .
Wieś powstała na polach mieszczan drohickich .
Według miejscowej tradycji pierwszym mieszkańcem był flisak Skudlarski , retman z Serocka .
Z rozbitej berlinki wzniósł tu w XIX wieku pierwszy dom .
