Bychawa
Bychawa – miasto w Polsce , w województwie lubelskim , w powiecie lubelskim , siedziba gminy miejsko-wiejskiej Bychawa .
Położona na Wyżynie Lubelskiej , 30 km na południe od Lublina nad rzekami Gałęzówką i Kosarzewką .
Według danych GUS z 31 grudnia 2019 r. Bychawa liczyła 4855 mieszkańców .
Położenie .
Według danych z 1 stycznia 2011 powierzchnia miasta wynosiła 6,69 km² .
Bychawa położona jest w historycznej Małopolsce , początkowo należała do ziemi sandomierskiej , a następnie do ziemi lubelskiej .
