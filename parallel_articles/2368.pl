село , trb .
sieło ) w zachodniej Rosji , w sielsowiecie baninskim rejonu fatieżskiego w obwodzie kurskim .
Geografia .
Miejscowość położona jest nad Rżawcem ( dopływ Krasawki w dorzeczu Swapy ) , 5 km od centrum administracyjnego sielsowietu ( Czermosznoj ) , 9 km na północny zachód od centrum administracyjnego rejonu ( Fatież ) , 54 km na północny zachód od Kurska , 0,5 km od drogi magistralnej ( federalnego znaczenia ) M2 « Krym » ( część trasy europejskiej E105 ) .
We wsi znajduje się 110 posesji .
Miejscowość , jak i cały rejon , znajduje się w strefie klimatu kontynentalnego z łagodnym ciepłym latem i równomiernie rozłożonymi opadami rocznymi ( Dfb w klasyfikacji klimatów Köppena ) .
Demografia .
W 2010 r. wieś zamieszkiwało 175 osób .
