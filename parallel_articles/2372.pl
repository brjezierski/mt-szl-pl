Bykowice
Bykowice ( niem .
`` Beigwitz '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Pakosławice .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Bichowitz '' w szeregu wsi lokowanych na prawie polskim `` iure polonico '' .
Na terenie miejscowości znajduje się przystanek kolejowy Myśliczyn z jednym peronem jednokrawędziowym .
Przystanek został zbudowany w czynie społecznym w 1975 r. Nazwa przystanku powstała z połączenia słów „ myśl ” i „ czyn ” ( ponieważ myślą przewodnią jego powstania był czyn ) .
