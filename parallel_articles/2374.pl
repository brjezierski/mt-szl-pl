Bylice ( Ukraina )
Bylice ( ukr .
Биличі ) – wieś w rejonie starosamborskim obwodu lwowskiego Ukrainy , nad Błozewką Starą .
Wieś liczy około 848 mieszkańców .
Podlega lutowiskiej silskiej radzie .
Wieś położona w powiecie przemyskim , była własnością Jana Lipskiego , została spustoszona w czasie najazdu tatarskiego w 1672 roku .
W 1921 r. liczyły około 1310 mieszkańców .
W II Rzeczypospolitej miejscowość była siedzibą gminy wiejskiej Bylice w powiecie samborskim .
W 1939 roku urodził się tutaj Bolesław Żuk – polski zootechnik , profesor .
