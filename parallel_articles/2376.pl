Będominek
Będominek ( dodatkowa nazwa w j. ; ) – wieś kaszubska w Polsce na Pojezierzu Kaszubskim położona w województwie pomorskim , w powiecie kościerskim , w gminie Kościerzyna .
Wieś jest częścią składową sołectwa Zielenin .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
