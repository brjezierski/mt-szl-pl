Żarówka
Żarówka , lampa żarowa – elektryczne źródło światła , w którym ciałem świecącym jest silnie rozgrzane przepływem prądu włókno wykonane z trudno topliwego materiału ( pierwotnie grafit , obecnie wolfram ) .
Drut wolframowy jest umieszczony w szklanej bańce wypełnionej mieszaniną gazów szlachetnych ( np .
argon z 10-procentową domieszką azotu ) lub próżnią .
Włókno osiąga temperaturę 2500–3000 K. Pierwsze lampy żarowe konstruowano w połowie XIX w .
Standardowa żarówka składa się z bańki typu A ( kształt gruszki ) oraz trzonka E27 .
Bańka typu A często oznaczana jest skrótowcem GLS ( ang .
`` General Lighting Service '' ) , natomiast trzonek E27 oznaczany ES ( ang .
`` Edison screw '' ) .
Najpowszechniej stosowane są żarówki z bańkami A55 i A60 oraz trzonkiem E27 ( typ bańki – A , max .
średnica baniek – 55 i 60 mm ; gwint Edisona – E , zew .
średnica gwintu – 27 mm ) .
