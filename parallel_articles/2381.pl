Byron Bay
Byron Bay – miasto w Australii , w stanie Nowa Południowa Walia , położone nad zatoką o tej samej nazwie , u nasady Przylądka Byrona , najbardziej na wschód wysuniętego punktu kraju .
W 2011 roku liczyło 4959 mieszkańców .
W 1770 roku jako pierwszy Europejczyk dotarł tu James Cook , który nazwał przylądek na cześć komandora Johna Byrona ( dziadka pisarza Lorda Byrona ) .
Osada założona została w 1860 roku i rozwinęła się jako port drzewny .
W 1896 roku stała się miastem .
Miasto było głównym portem na przybrzeżnej trasie żeglugowej z Newcastle ( około 630 km na południe ) do Brisbane ( 140 km na północ ) .
W latach 50 .
XX wieku było portem wielorybniczym .
Współcześnie istotną rolę w lokalnej gospodarce odgrywają turystyka , rolnictwo ( hodowla bydła , uprawa kukurydzy , bananów , ananasów i awokado ) , przemysł spożywczy ( produkcja mięsa , nabiału , przetwórstwo ryb ) oraz wydobywczy ( wydobycie cyrkonu i rutylu ) .
