Oranżówka wrzecionowatozarodnikowa
Oranżówka wrzecionowatozarodnikowa ( `` Byssonectria fusispora '' ( Berk . )
Rogerson & amp ; Korf ) – gatunek grzybów z rodziny `` Pyronemataceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Byssonectria , Pyronemataceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1846 r. Miles Joseph Berkeley , nadając mu nazwę `` Peziza fusispora '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1971 roku Clark Thomas Rogerson i Richard Paul Korf .
