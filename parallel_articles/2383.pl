Bystra ( skała )
Bystra – skała w Ojcowskim Parku Narodowym .
Znajduje się na orograficznie lewym , zalesionym zboczu Doliny Prądnika , tuż naprzeciwko stawów z hodowlą pstrąga .
Ze skały Jonaszówka w zboczu tym widoczne są 4 grupy skał ; kolejno od lewej są to : Czyżówki , Figowe Skały , Ostrogi i Bystra .
Bystra to olbrzymi , zbudowany z wapieni masyw , do Doliny Prądnika opadający pionowymi , a w niektórych nawet miejscach przewieszonymi ścianami .
Dobrze widoczne są na nim spękania ciosowe .
Z jej szczytu rozciąga się szeroka panorama widokowa na Dolinę Sąspowską .
