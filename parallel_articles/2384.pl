Bystrzanów
Bystrzanów – wieś w Polsce położona w województwie mazowieckim , w powiecie piaseczyńskim , w gminie Tarczyn .
Wieś duchowna położona była w drugiej połowie XVI wieku w powiecie tarczyńskim ziemi warszawskiej województwa mazowieckiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa warszawskiego .
