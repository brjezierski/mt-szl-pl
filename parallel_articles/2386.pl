Kolonia Bystrzycka
Kolonia Bystrzycka – wieś w Polsce położona w województwie lubelskim , w powiecie łukowskim , w gminie Wojcieszków .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Pana Jezusa w Wojcieszkowie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
