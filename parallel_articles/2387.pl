Historia .
Sztućce swoją historią sięgają czasów pierwotnych człowieka .
Najstarsze znaleziska wskazują , iż pierwsze w użyciu znalazły się łyżki , które służyły nabieraniu najczęściej gorącego pokarmu .
Noże przyszły z czasem , przekształcając się następnie w widelce .
Kronikarze niemieccy wspominają o tym , że cesarzowa Teofano używała podwójnego szpikulca do nabijania nań jedzenia .
Pierwotnie sztućce wykonywane były z drewna – szczególnie w ubogich rodzinach .
Tylko nieliczni mogli pozwalać sobie na metalową zastawę i akcesoria , które w wieku XVI wykonywane były przez płatnerzy .
Za wyraz największej elegancji uważana jest srebrna zastawa w tym i sztućce wykonane z tegoż metalu , mimo to popularne w świecie były inne rodzaje metali jak pierwotnie miedź , cynk , aluminium i ołów .
