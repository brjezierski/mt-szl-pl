Sztućce
Sztućce – przybory ręczne służące do jedzenia oraz do przygotowania potraw .
Sztućce są elementem zastawy stołowej .
W zależności od kształtu i wielkości przeznaczone są do różnych czynności konsumpcyjnych .
Rodzaje sztućców przypisane są odpowiednim potrawom .
Nazwa sztućce pochodzi od włoskiego słowa stuccio .
