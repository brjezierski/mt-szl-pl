Bytnik
Bytnik – wieś sołecka w Polsce położona w województwie dolnośląskim , w powiecie głogowskim , w gminie Głogów , po lewej stronie Odry .
Wieś typowo rolnicza .
Podział administracyjny .
W latach 1975–1998 miejscowość położona była w województwie legnickim .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 72 mieszkańców .
