Księstwo bytomskie
Księstwo bytomskie ( czes .
`` Bytomské knížectví '' , niem .
`` Herzogtum Beuthen '' ) – księstwo śląskie powstałe w 1284 roku w wyniku podziału Księstwa Opolskiego , od 1289 roku stało się lennem króla czeskiego .
Granice do podziałów .
W 1281 zmarł Władysław opolski .
