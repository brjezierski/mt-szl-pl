W 1284 Bolko i Kazimierz dokonali podziału pomiędzy Księstwa .
Bolko został samodzielnym księciem na Opolu , Niemodlinie i Strzelcach , natomiast Kazimierz został księciem na Bytomiu , Koźlu i Toszku , jako książę bytomski .
W czasach księcia Kazimierza , Księstwo Bytomskie obejmowało największe terytorium w swojej historii .
Północny odcinek granicy biegł nieco na południe od Małej Panwi , od okolic Baruta na zachód .
W dwóch miejscach – na południe od Lublińca oraz na północ od Miasteczka Śląskiego – rzeka była granicą z Księstwem opolskim .
