Siedzibą władz gminy była wieś Nowy Bytom ( do 1922 część miasta Bytomia w Niemczech , 1947–1958 samodzielne miasto , od 1959 dzielnica Rudy Śląskiej ) .
Gmina zbiorowa ( o charakterze jednostkowym ) Nowy Bytom powstała w grudniu 1945 w powiecie katowickim w woj .
śląskim ( śląsko-dąbrowskim ) .
1 stycznia 1946 gmina składała się z samej siedziby i przez to nie była podzielona na gromady .
Jako gmina wiejska jednostka przestała istnieć 1 stycznia 1947 wraz z nadaniem Nowemu Bytomiowi praw miejskich i przekształceniem jednostki w gminę miejską .
W związku z likwidacją powiatu katowickiego 1 kwietnia 1951 Nowy Bytom został powiatem grodzkim , a następnie 31 grudnia 1958 wszedł w skład Rudy Śląskiej .
