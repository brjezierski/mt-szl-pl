Bytów
Bytów ( dodatk .
nazwa w j. kaszub .
Bëtowò ; niem .
`` Bütow '' ) – miasto w północnej Polsce , w województwie pomorskim , w powiecie bytowskim , siedziba gminy miejsko-wiejskiej Bytów , położone na Pojezierzu Bytowskim , nad rzekami Borują oraz Bytową i jeziorem Jeleń .
Miasto stanowi lokalny węzeł drogowy ( DK20 , DW209 , DW212 , DW228 ) .
W latach 1975–1998 miasto administracyjnie należało do województwa słupskiego .
Według danych GUS z 30 czerwca 2021 r. , Bytów liczył 16 790 mieszkańców i był pod względem liczby ludności szesnastym miastem w województwie pomorskim .
Ośrodek turystyczno-wypoczynkowy .
Przemysł elektrotechniczny , spożywczy , drzewny i metalowy .
Historia .
W 1294 roku zmarł książę gdański Mściwoj II , w związku z czym jego dziedzictwo objął późniejszy król Polski Przemysł II .
W 1307 roku obszar , na którym leży Bytów został zajęty przez margrabiów brandenburskich .
W 1317 roku brandenburski margrabia Waldemar przekazał ziemię bytowską księciu zachodniopomorskiemu Warcisławowi IV , a ten w 1321 roku nadał ziemię bytowską swojemu marszałkowi Henningowi Behrowi .
Z 1321 roku pochodzi najstarszy zapis nazwy miasta w formie `` Butow '' .
W 1329 ziemia bytowska została wykupiona od jego spadkobierców przez Krzyżaków za 800 grzywien denarów pruskich srebra i następnie utworzyli w Bytowie prokuratorstwo .
W 1342 roku Krzyżacy utracili Słupsk , dlatego przenieśli z niego na krótko komturstwo do Bytowa .
Prawa miejskie osada otrzymała 12 lipca 1346 .
W latach 1390–1405 Krzyżacy odbudowali zamek , w 1409 gród został zdobyty przez wojska polskie .
W roku 1410 po bitwie pod Grunwaldem został przekazany przez króla Władysława Jagiełłę księciu pomorskiemu Bogusławowi VIII .
W 1411 roku na mocy pokoju toruńskiego ziemia bytowska wróciła do Krzyżaków .
