.kh
.kh – domena internetową przypisaną dla stron internetowych z Kambodży .
Nazwa pochodzi od największej grupy etnicznej kraju – Khmerów .
Dawniej , od 1998 roku była przyznawana przez Ministerstwo Poczty i Telekomunikacji Kambodży .
Od września 2012 roku ta domena jest administrowana przez Regulatora Telekomunikacyjnego Kambodży .
