Błaszki
Błaszki ( ) – miasto w Polsce , w województwie łódzkim , w powiecie sieradzkim , siedziba gminy miejsko-wiejskiej Błaszki .
Przez miasto przepływa rzeka Trojanówka .
Błaszki uzyskały lokację miejską przed 1722 .
Według danych GUS z 31 grudnia 2019 r. miasto liczyło 2085 mieszkańców .
