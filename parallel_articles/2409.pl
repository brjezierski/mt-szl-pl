Błażejowice Dolne
Błażejowice Dolne ( dodatkowa nazwa w j. niem .
Blaschewitz ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 150 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 9,5 km od granicy z Czechami , na Wysoczyźnie Bialskiej .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Środowisko naturalne .
W Błażejowicach Dolnych panuje klimat umiarkowany ciepły .
Średnia temperatura roczna wynosi +8,3 °C .
