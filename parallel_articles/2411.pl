Błogoszcz
Błogoszcz – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Przez wieś przebiega droga powiatowa Golice - Pruszyn oraz Błogoszcz - Stok Lacki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Od czerwca 1985 roku funkcjonuje w miejscowości Ochotnicza Straż Pożarna .
Inicjatorkami jej powstania były mieszkanki wsi .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Mikołaja w Pruszynie .
