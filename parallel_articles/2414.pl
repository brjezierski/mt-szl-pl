Brzecław
Brzecław ( czes .
`` Břeclav '' , niem .
`` Lundenburg '' ) – miasto w Czechach , w kraju południowomorawskim , siedziba powiatu Brzecław .
Leży 50 km na południowy wschód od Brna , w pobliżu granicy z Austrią i Słowacją , nad rzeką Dyją .
Jego powierzchnia wynosi 87,17 km² i liczy około 26000 mieszkańców .
Historia .
Miasto zostało założone w XI wieku w pobliżu grodu wybudowanego przez księcia Brzetysława ( stąd też nazwa miasta ) .
Gród ten stał się nawet na krótko w XII wieku siedzibą samodzielnego księstwa .
W latach 1426–1434 zamek był siedzibą wojsk husyckich , które urządzały wyprawy na ziemie austriackie i za pobliską granicę Węgier .
W XVI wieku Brzecław przeszedł do majątku starego morawskiego rodu Žerotínów , którzy przebudowali zamek w stylu renesansowym i powiększyli miasto .
Po bitwie na Białej Górze zamek przejął ród Lichtensteinów .
Ważnym wydarzeniem w historii miasta była budowa linii kolejowej z Wiednia do Brna w roku 1839 .
Po budowie kolejnych linii do Przerowa w 1841 , Znojma w 1872 , Lednic w 1901 i Kút w 1909 , miasto stało się ważnym węzłem kolejowym , co przyczyniło się do znacznego rozwoju przemysłu i napływu ludności .
Zabytki i atrakcje turystyczne .
W okolicy miasta znajduje się krajobraz kulturowy Lednice-Valtice wpisany na listę światowego dziedzictwa UNESCO .
Gospodarka .
W mieście rozwinął się przemysł chemiczny , maszynowy , ceramiczny oraz spożywczy .
