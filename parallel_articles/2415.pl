Bóg Ojciec
Bóg Ojciec , Pan Bóg ( łac .
`` Deus Pater '' ) – jedna z Trzech Osób Boskich , czczona ( łac .
`` Latriae cultus '' ) przez większość wyznań chrześcijańskich .
Tożsamy z hebrajskim , pierwotnie utożsamianym ze srogim , ale i sprawiedliwym sędzią .
Jest on Ojcem drugiej Osoby Trójcy – Słowa Bożego ( Logos ) wcielonego w człowieka Jezusa Chrystusa , według wiary chrześcijańskiej „ z Ojca zrodzonego przed wszystkimi wiekami , zrodzonego , nie stworzonego ” .
Od Ojca i Syna ( według prawosławia od Ojca przez Syna ) pochodzi trzecia Osoba Trójcy – Duch Święty .
Według teologii części chrześcijaństwa , która przyjmuje interpretację Trójcy Świętej , Bóg Ojciec , Syn Boży i Duch Święty to jedna substancja – Bóg , ale równocześnie trzy oddzielne Osoby ( hipostazy ) .
Poprzez stworzenie człowieka , a następnie wcielenie Jezusa Chrystusa i odkupienie ludzkości , Bóg Ojciec jest również określany przez chrześcijan jako „ Ojciec ” wszystkich ludzi .
