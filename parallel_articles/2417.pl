Mumbaj
Mumbaj ( nazwa główna ) , Bombaj ( egzonim wariantowy ) ( , trb .
: `` Mumbaj '' ; ; do 1995 ) – stolica indyjskiego stanu Maharasztra , położona na wyspie Salsette , na Morzu Arabskim .
Wraz z miastami satelitarnymi tworzy najludniejszą po Delhi aglomerację liczącą 23 miliony mieszkańców .
Dzięki naturalnemu położeniu jest to największy port morski kraju .
Znajdują się tutaj także najsilniejsze giełdy Azji Południowej : National Stock Exchange of India i Bombay Stock Exchange .
Mumbaj jest ponadto uznawany za stolicę kinematografii indyjskiej .
Nazewnictwo .
Indyjska nazwa pochodzi od miejscowej hinduskiej bogini `` Mumba '' .
W XVI wieku Portugalczycy nazwali to miasto `` Bom Bahia '' ( „ Dobra Zatoka ” ) .
Brytyjczycy po przejęciu kolonii w 1661 zmienili nazwę miasta na .
Oficjalna zmiana nazwy w języku hindi i angielskim nastąpiła w 1995 .
Komisja Standaryzacji Nazw Geograficznych jako polską nazwę aprobuje zarówno określenie „ Mumbaj ” , jak i „ Bombaj ” .
Historia .
Obszar Bombaju był już prawdopodobnie zasiedlony w epoce kamienia .
Od III w. p.n.e .
należał on do rządzonego przez buddystów Imperium Maurjów .
Od 1343 był w posiadaniu państwa Gujarat .
Najstarsze budowle w mieście – słoniowe jaskinie , czy kompleks świątyń Walkeśwary , pochodzą właśnie z tamtego okresu .
W 1534 roku miasto zajęli Portugalczycy .
W 1661 królowa Katarzyna Bragança przekazała miasto angielskiemu królowi Karolowi II .
W 1668 zostało ono wydzierżawione Brytyjskiej Kompanii Wschodnioindyjskiej za cenę 10 funtów rocznie .
Miasto zaczęło się szybko rozwijać jako port .
Ludność wzrosła z 10 000 w 1661 do 60 000 w 1675 .
W 1687 siedziba Brytyjskiej Kompanii Wschodnioindyjskiej została przeniesiona z miasta Surat do Bombaju .
W 1817 powstał plan połączenia kilku wysp przybrzeżnych z lądem , dzięki czemu uzyskano by większą powierzchnię miasta .
Projektem pokierował inżynier Hornby Vellard .
Prace ukończono w 1845 i otrzymano powierzchnię 435 km² .
W 1853 Bombaj połączono linią kolejową z miastem Thana .
W czasie wojny secesyjnej miasto stało się głównym centrum handlu bawełną na świecie .
Przyczyniło się to do wielkiego boomu gospodarczego Bombaju .
Kiedy ukończono Kanał Sueski ( 1869 ) miasto stało się największym portem na Morzu Arabskim .
Były to lata wielkiego rozwoju Bombaju .
Już w 1906 w mieście mieszkało milion osób i było ( po Kalkucie ) 2 największym miastem Indii pod względem ludności .
W późniejszym czasie stało się ono centrum indyjskiej walki o niepodległość .
W 1942 Mahatma Gandhi wezwał do manifestacji antybrytyjskich .
Rząd w tym samym roku wystąpił przeciwko Brytyjczykom z hasłem `` Opuśćcie Indie '' ( `` Quit India '' ) .
Od 1960 stał się stolicą stanu Maharasztra .
Od 1970 w Bombaju znów nastąpił boom ludnościowy – miasto przyciągało imigrantów z całych Indii .
W 1986 Bombaj miał podobną ludność co Kalkuta .
W 1992 miały miejsce zamieszki skierowane przeciwko ludności muzułmańskiej , które spowodowały liczne ofiary śmiertelne .
Kilka miesięcy później , 12 marca , w licznych zamachach bombowych przeprowadzonych jednego dnia zginęło 300 osób ( zamieszki w Bombaju ) .
W 2005 miasto zostało poważnie podtopione na skutek bardzo obfitych deszczów monsunowych .
11 lipca 2006 roku terroryści zaatakowali mumbajską kolej .
W ciągu niespełna kwadransa w różnych pociągach na trasie zachodniej eksplodowało siedem bomb .
Zginęło ponad 200 osób , a około 800 zostało rannych .
26 listopada 2008 w mieście miała miejsce seria zamachów terrorystycznych , w których zginęło co najmniej 195 osób , a 295 zostało rannych .
Geografia .
Miasto umieszczone jest na wyspie Salsette , przy ujściu rzeki Ulhas .
Średnia wysokość miasta wynosi od 10 do 15 m n.p.m .
Północna część metropolii jest natomiast pagórkowata , z najwyższym szczytem miasta sięgającym 450 m n.p.m .
Całkowita powierzchnia Mumbaju wynosi 438 km² .
Na terenie miasta znajdują się 3 jeziora : Tulsi , Vihar i Powai .
Pierwsze dwa należą do Parku Narodowego Borivali i są głównymi źródłami wody słodkiej dla miasta .
Linia brzegowa jest urozmaicona licznymi zatoczkami i lagunami .
Wschodnią część wybrzeża morskiego zajmują zarośla mangrowe , bogate w faunę i florę .
W mieście ziemia składa się przeważnie z piasku ( bliskość morza ) .
Natomiast na przedmieściach ziemia jest żyzna ( gleby aluwialne ) .
Osady te pochodzą głównie z wyżyny Dekan .
Mumbaj leży w strefie aktywnej sejsmicznie .
Mogą się tu zdarzyć trzęsienia ziemi o sile do 6,5 stopnia w skali Richtera .
Mumbaj dzieli się na dwie główne dzielnice : miasto ( City ) i suburbia , te dzielą się na 6 stref i 24 okręgi .
Klimat .
Mumbaj leży w pasie klimatów tropikalnych , więc występują tu tylko 2 pory roku : sucha i wilgotna .
Pora wilgotna trwa od marca do października .
Charakteryzuje ją duża wilgotność i temperatury przekraczające 30 °C .
Pora monsunowa trwa od czerwca do września i średnie opady w tym czasie wynoszą 2200 mm .
Rekord ilości opadów zanotowano w 1954 roku ( 3 451,6 mm ) .
Pora sucha trwa od listopada do lutego .
