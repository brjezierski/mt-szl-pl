Cukierek
Cukierek ( właściwie cukier kandyzowany ) – wyrób cukierniczy wykonany ze stężonego roztworu cukru w wodzie , do którego dodawane są aromaty i barwniki .
Słowa „ cukierek ” zaczęto używać w XVIII wieku , wówczas to w polszczyźnie zaczęły się rozpowszechniać derywaty od wyrazów oznaczających surowce jadalne , w tym przypadku podstawą był „ cukier ” .
Wyrób .
Cukierki otrzymuje się przez rozpuszczenie cukru w wodzie lub mleku do postaci syropu , który gotuje się aż do osiągnięcia pożądanego stężenia lub początku karmelizacji .
Typ cukierka zależy od składników i czasu podgrzewania mikstury .
Cukierki to słodycze o różnorodnej konsystencji i różnym sposobie produkcji , są dostępne prawie we wszystkich kolorach , mają najprzeróżniejsze kształty i fakturę .
Występują w wielu wariantach smakowych , m.in .
o smaku kwaśnym , słodkim , słonym , owocowym , kawowym czy miętowym , często w postaci wieloowocowych żelek .
Większość cukierków zawiera w sobie konserwant .
Przykładami cukierków są : cukierki toffi , krówki , pralinki , dropsy , landrynki , cukierki ziołowe .
Ostatnie z wymienionych można konsumować jako przysmak lub też jako środek leczniczy , głównie przeciwkaszlowy .
Przy wyrobie cukierków ziołowych stosuje się wywary z ziół takich jak żywokost , ślaz i podbiał oraz roślinne olejki eteryczne .
W związku z tym wyroby te posiadają specyficzny smak i pewne właściwości lecznicze .
Etapy tworzenia .
Ostateczna faktura cukierka zależy od stężenia cukru .
Gdy syrop jest podgrzewany , gotuje się , woda paruje i zwiększa stężenie cukru .
Określonej temperaturze odpowiada szczególna koncentracja cukru .
W wyższych temperaturach wyrabia się cukierki kruche , a w niższych cukierki miękkie .
Etapami gotowania cukru są :
Nazwy pochodzą od procesu stosowanego do testowania syropu przed osiągnięciem przystępnej temperatury : małą łyżkę syropu wlewa się do zimnej wody i określa się stężenie syropu .
