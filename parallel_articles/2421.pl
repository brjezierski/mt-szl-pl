Bumsztak
Bumsztak ( wspieracz ) – długi , ok. pięciometrowy drąg o grubości ok. 18 cm , z jednej strony zakończony rączką , służący do tzw .
„ bumsztakowania ” , czyli manewrowania statkiem na płytkich wodach poprzez odpychanie się od dna .
Wykorzystuje się go również podczas postoju statku , jeździe w górę rzeki przy płytkiej wodzie i odpychania się od brzegu .
Bumsztaki znajdują szerokie zastosowanie w statkach bez napędu , np .
barkach .
Marynarz obsługujący bumsztak powinien być uprzednio dokładnie przeszkolony , bowiem operowanie nim wymaga zarówno dużej siły , jak i precyzji .
Należy go zarzucić na dno rzeki ukośnie do kierunku jazdy statku .
Aby zapobiec utknięciu bumsztaka w mulistym dnie , do jego końca przywiązuje się cienką , stalową linkę , połączoną z pokładem .
Wchodzi on wtedy w dno jedynie w takim stopniu , na jaki pozwala mu linka , potem na skutek ruchu statku odrywa się on od dna .
Specjalny rodzaj bumsztaku , tzw .
laski , wykorzystuje się w przepychaniu barki do śluzy .
Są one zwykle dużo lżejsze i cieńsze .
