Bziniczka
Bziniczka ( `` Brzyniczka '' ) – potok , prawy dopływ Małej Panwi o długości 19,75 km .
Potok płynie początkowo w województwie śląskim , a następnie w województwie opolskim .
Wypływa z okolic Gwoździan .
Źródło Bziniczki zlokalizowane jest na wysokości ok. 270 m n.p.m .
Płynie w większości w leśnym otoczeniu uregulowanym korytem o szerokości 2-3 m. Na zachód od Pluder opuszcza teren gminy Dobrodzień i wpływa do gminy Kolonowskie .
Na tym odcinku charakteryzuje się zbliżonym do naturalnego przebiegiem koryta o średniej szerokości 3 m. Bziniczka uchodzi do Małej Panwi w Kolonowskiem na wysokości 190 m n.p.m .
W gminie Kolonowskie wokół rzeki stworzony został zespół przyrodniczo-krajobrazowy `` Nad Bziniczką '' .
Od rzeki wywodzi się nazwa wsi Bzinica Stara , przez którą ona przepływa .
