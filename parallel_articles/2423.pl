C ( język programowania )
C – imperatywny , proceduralny język programowania ogólnego przeznaczenia , stworzony na początku lat 70 .
XX wieku przez Dennisa Ritchiego , ówczesnego pracownika Bell Labs .
W języku C powstały narzędzia systemowe dla Uniksa , a potem również kod systemu Unix .
Język C został ustandaryzowany w 1989 roku przez ANSI .
Od tego czasu jego rozwojem zajmuje się grupa robocza w ramach ISO .
Język C powstawał jako rozwinięcie języka B , wzbogacając się stopniowo o kolejne funkcje .
Okresy najszybszego rozwoju języka C to lata 1972–1973 oraz 1977–1979 .
Z kolei lata 1980. to czas zdobywania przez niego popularności , czego efektem była dostępność kompilatorów dla praktycznie wszystkich używanych wtedy architektur komputerów i systemów operacyjnych .
C jest jednym z popularniejszych języków programowania .
Służy zarówno do pisania elementów systemów operacyjnych , jak i aplikacji użytkowych .
Mimo to spotyka się z krytyką z powodu pewnych decyzji projektowych , takich jak priorytety operatorów lub nadmiernie lakoniczna składnia .
Historia .
Początki C są ściśle związane z rozwojem systemu Unix , napisanego pierwotnie przez Dennisa Ritchiego i Kena Thompsona w asemblerze na komputer PDP-7 .
Późniejsza wersja systemu , przeznaczona na maszynę PDP-11 , również powstała w asemblerze .
Thompson czuł potrzebę wykorzystania języka wyższego poziomu do napisania narzędzi systemowych dla Uniksa .
Początkowo próbował przygotować kompilator Fortranu , lecz dość szybko porzucił ten pomysł .
W zamian stworzył własną , okrojoną wersję BCPL , którą nazwał B. W języku tym powstało niewiele narzędzi , ponieważ wynikowe programy były powolne oraz nie mogły korzystać z adresowania poszczególnych bajtów pamięci ( funkcji dostępnej np .
