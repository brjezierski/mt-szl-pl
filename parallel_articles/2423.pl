Wczesny rozwój .
Dennis Ritchie rozpoczął w 1972 roku rozwijanie języka B. Jedną z pierwszych zmian było dodanie typów danych dla zmiennych .
Początkowo dostępne były dwa : znakowy ( codice_1 ) i całkowitoliczbowy ( codice_2 ) .
Wprowadzono również sposób deklarowania zmiennych typu wskaźnikowego oraz odwołań do funkcji .
Tak znaczące zmiany spowodowały , że Ritchie nadał językowi nową nazwę : C .
Zgodnie z sugestią Alana Snydera , na początkowym etapie rozwoju języka Ritchie rozdzielił operatory koniunkcji i alternatywy na bitowe ( składające się z pojedynczego symbolu ) oraz logiczne ( złożone z dwóch znaków ) .
W języku B oba warianty oznaczano w taki sam sposób , a odpowiednie działanie było wybierane przez interpreter , w zależności od kontekstu .
Kompilator C , wraz z kilkoma programami napisanymi w tym języku , został dołączony do drugiej wersji systemu Unix .
Latem 1973 jądro tego systemu zostało przepisane w języku C .
Do tego czasu wprowadzono również do składni języka obsługę struktur .
Mniej więcej w tym samym czasie powstała pierwsza wersja preprocesora .
