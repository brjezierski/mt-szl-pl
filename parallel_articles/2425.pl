Caacupé
Caacupé – miasto w departamencie Cordillera , w Paragwaju .
Według danych na rok 2020 miasto zamieszkiwało 57682 osoby , a gęstość zaludnienia wyniosła 416,2 os./km2 .
Miasto jest siedzibą diecezji Caacupé .
Caacupé jest najbardziej znany jako miejsce statuetki Matki Boskiej , która mieści się w ogromnej bazylice , która stoi w centrum miasta .
