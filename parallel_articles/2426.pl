Cabedelo
Cabedelo – miasto w Brazylii , w stanie Paraíba , położone nad Atlantykiem i rzeką Paraíba .
Opis .
Miejscowość została założona w 1956 roku .
Obecnie miasto wchodzi w skład obszaru metropolitalnego João Pessoa .
Cabedelo jest punktem początkowym drogi krajowej transamazońskiej BR-230 i linii kolejowej do João Pessoa .
W mieście znajduje się port morski zajmujący się przeładunkiem towarów .
