Caen
Caen – miasto i gmina w północnej Francji , stolica historycznej Normandii i ośrodek administracyjny regionu Normandia oraz departamentu Calvados .
Według danych na rok 1990 gminę zamieszkiwało 112 846 osób , a gęstość zaludnienia wynosiła 4391 osób/km² ( wśród 1815 gmin Dolnej Normandii Caen plasuje się na 1. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 56 . ) .
W aglomeracji mieszkało około 220 tysięcy osób .
Caen leży nad rzeką Orne , w pobliżu jej ujścia do kanału La Manche .
Przemysł samochodowy , elektroniczny , farmaceutyczny , papierniczy i cementowy .
