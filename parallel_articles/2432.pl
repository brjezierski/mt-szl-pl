Cagliari
Cagliari [ czyt .
'kaljari ] ( sard .
`` Casteddu '' – `` zamek '' ) – miasto i gmina we Włoszech , stolica autonomicznego regionu Sardynia i miasta metropolitalnego Cagliari .
Miasto Cagliari znajduje się na południowym wybrzeżu Sardynii , nad zatoką o tej samej nazwie i na równinie Campidano .
Według danych na rok 2021 gminę zamieszkiwało 149 572 osób .
Gospodarka .
Cagliari jest ośrodkiem gospodarczym i przemysłowym , ma jeden z największych portów na Morzu Śródziemnym ( handlowy , rybacki i wojenny ) oraz międzynarodowe lotnisko .
Zajmuje 28 miejsce co do wielkości dochodu we Włoszech .
