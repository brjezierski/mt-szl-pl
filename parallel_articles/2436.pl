Caissa
Caissa – driada przedstawiana jako bogini szachów .
Mit nie występował w starożytności , a jako pierwszy powiązanie między Caissą a szachami utworzył William Jones w poemacie `` Caissa or the games of chess '' ( Caissa czyli partia szachów ) , wydanym w Oxfordzie w 1772 .
Fabuła poematu opisuje partię szachów , graną na Olimpie w obecności bogów przez dwie nimfy .
Podczas gry pojawiła się na Olimpie Caissa , wzbudzając zachwyt swą urodą .
Szczególnie zauroczony jej wdziękami był Mars .
Dowiedziawszy się , gdzie nimfa mieszka , aby ją zdobyć przyniósł tam szachownicę o polach wykonanych ze srebrnych i złotych płytek i zaproponował jej rozegranie partii .
Caissa nie znała jednak zasad tej gry , poprosiła zatem Marsa , by ją ich nauczył .
Nie wiadomo , czy zaloty Marsa przyniosły oczekiwany przez niego skutek , gdyż w kulminacyjnym momencie akcja poematu przenosi się z powrotem na Olimp .
Nie wiadomo , kto pierwszy utożsamił Caissę z boginią szachów .
Obecnie imię bogini jest często przywoływane przez prasę szachową i sportową , także szachiści mówią , iż `` Caissa im sprzyjała '' ( lub też nie sprzyjała ) .
Nazwę Kaissa nosił rosyjski program szachowy , zwycięzca Mistrzostw świata programów komputerowych z 1974 roku , rozgrywanych w Sztokholmie .
Rosyjski szachista i gracz w warcaby Borys Feldman stworzył `` pojedynek bogiń Caissy i Minerwy '' , czyli partię , w której figury szachowe grają z pionkami warcabów .
