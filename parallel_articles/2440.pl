Czasopismo
Czasopismo – wydawnictwo ciągłe , periodyczne , publikowane pod niezmienionym tytułem , posiadające numerację ciągłą , zawierające ustaloną szatę graficzną , a także niezbyt często zmieniający się format i objętość , zawierające materiały od wielu autorów i określoną tematykę , aczkolwiek mogące różnić się wersjami językowymi – może występować w wielu mutacjach regionalnych .
Potocznie za czasopisma uważa się publikacje od tygodnika wzwyż , te wydawane częściej nazywane są gazetami .
Rodzaje czasopism .
Ze względu na częstotliwość wydań wyróżnia się najczęściej , następujące rodzaje :
Wydawnictwa rzadsze niż kwartalne ( np .
półrocznik , rocznik , annał ) określa się często jako edycje kolejnych zeszytów , wydań lub tomów .
Ponadto czasopisma mogą być : ogólnotematyczne , branżowe , reklamowe , recenzowane , itd .
