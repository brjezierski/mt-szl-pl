Gazeta
Gazeta – rodzaj wydawnictwa ciągłego .
Ukazuje się częściej niż raz w tygodniu , najczęściej codziennie ( dziennik ) – w rozumieniu wszystkich dni roboczych .
Nazwa .
Pochodzenie słowa „ gazeta ” nie jest do końca jasne .
Wywodzi się przypuszczalnie z włoskiego `` gazzetta '' – określenia drobnej monety , za którą nabywano dzienniki w XVII-wiecznej Wenecji .
Słowo `` gazza '' oznacza również srokę , której podobiznę często umieszczano w dziennikach włoskich jako symbol gadatliwości i plotkarstwa .
Być może też źródłosłów `` gazety '' to hebrajskie `` izgard '' , tłumaczone jako herold , goniec , zwiastun .
