Calabasas
Calabasas – miasto w hrabstwie Los Angeles w Kalifornii .
W 2010 było zamieszkiwane przez 23 058 osób .
Etymologia nazwy .
Nazwa Calabasas pochodzi prawdopodobnie od hiszpańskiego `` calabaza '' ( dynia , kabaczek bądź tykwa ) lub angielskiego słowa `` calabash '' również oznaczającego tykwę .
W Calabasas jest organizowany Pumpkin Festival odbywający się w październiku w celu uhonorowania dyni , od której przypuszczalnie pochodzi nazwa .
