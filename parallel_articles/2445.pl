Calatayud
Calatayud – miasto i gmina w Hiszpanii , we wspólnocie autonomicznej Aragonia , w prowincji Saragossa położone nad rzeką Jalón , w centrum gór Iberyjskich .
Jest to drugie co do wielkości miasto w prowincji po stolicy Saragossie i największe miasto w Aragonii .
Jest siedzibą comarki Comunidad de Calatayud .
Calatayud stało się pierwszą demokratyczną gminą w 1977 roku , ponieważ wybory odbyły się na dzień wcześniej niż w całej Hiszpanii , aby móc przygotować się do wizyty króla Jana Karola I .
