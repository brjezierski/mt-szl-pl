Calgary
Calgary – największe miasto kanadyjskiej prowincji Alberta , leżące na przedgórzu Gór Skalistych na skrzyżowaniu rzek Bow i Elbow .
Historia .
Założone w 1875 r. jako Fort Calgary .
Szybki rozwój Calgary nastąpił od wybudowania Kolei Transkanadyjskiej ( ang .
`` Canadian Pacific Railway '' ) .
Prawa miejskie zostały nadane osadzie w 1894 roku .
Dalszy rozwój miasta w wyniku odkrycia złóż ropy naftowej w 1914 i 1947 roku .
Obecnie Calgary jest głównym centrum korporacyjnym przemysłu ropy naftowej i gazu ziemnego , jest też ośrodkiem przemysłów : drzewnego , mięsnego i zbożowego .
