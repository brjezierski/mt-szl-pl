Callao
Callao – największy i najważniejszy port w Peru położony 12 km na zachód od stolicy państwa , Limy .
Callao jest jednym z 25 regionów Peru , składającym się z pojedynczej prowincji , która obejmuje 7 dystryktów .
Gospodarka .
W mieście rozwinął się przemysł spożywczy , chemiczny , drzewny , stoczniowy oraz hutniczy .
W Callao znajduje się port rybacki oraz międzynarodowy port lotniczy .
Historia .
Callao zostało założone przez Hiszpanów w 1537 roku i stało się jednym z najważniejszych portów handlowych na Pacyfiku .
W 1746 roku port został całkowicie zniszczony przez podmorskie trzęsienie ziemi .
