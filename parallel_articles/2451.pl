Calling Time
Calling Time ( w Meksyku wydany również jako Calling Time World ) – czwarty album studyjny szwedzkiego muzyka Basshuntera , wydany 13 maja 2013 roku .
Album promowany był singlami „ Saturday ” , „ Fest i hela huset ” , „ Northern Light ” , „ Dream on the Dancefloor ” , „ Crash & amp ; Burn ” oraz „ Calling Time ” .
Gościnnie na albumie wystąpiły : Dulce María , Kerry Katona oraz Sandra i inni .
Album znalazł się na 25. miejscu amerykańskiej listy Dance/Electronic Albums .
