Callistosporium pinicola
Callistosporium pinicola Arnolds – gatunek grzybów z rodziny `` Callistosporiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Callistosporium , Callistosporiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten został opisany dopiero w 2006 r. przez Eefa Arnoldsa .
Brak go w pracy W. Wojewody z 2003 r. , ale potem w Polsce został znaleziony .
Występowanie i siedlisko .
W 2009 r. w Europie `` C. pinicola '' była znana w Austrii .
Czechach , Francji , Niemczech , Włoszech , Holandii , Belgii , Szwajcarii i na Ukrainie .
W Polsce po raz pierwszy znaleziono ten gatunek w 2014 r. w Beskidzie Żywieckim , Wigierskim Parku Narodowym , oraz na zboczach Małej Pasieki w Bieszczadach .
Nowe stanowiska tego gatunku podaje także internetowy atlas grzybów .
`` C. pinicola '' została w nim zaliczona do gatunków chronionych i zagrożonych .
Saprotrof .
Rozwija się na silnie już spróchniałych pniakach i pniach drzew , głównie iglastych , zwłaszcza sosny pospolitej .
Stwierdzono jednak występowanie tego gatunku także na jodle pospolitej i świerku pospolitym i podano jeden przypadek występowania na drewnie drzew liściastych – prawdopodobnie na buku .
