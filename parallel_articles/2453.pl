Gęśnica brzoskwiniowa
Gęśnica brzoskwiniowa ( `` Rugosomyces persicolor '' ( Fr . )
Bon ) – gatunek grzybów z rodziny kępkowcowatych ( `` Lyophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Calocybe , Lyophyllaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1792 r. Elias Fries jako odmianę gęśnicy fiołkowej `` Agaricus ionides '' var .
`` persicolor '' ) .
W 1962 r. Rolf Singer przeniósł go do rodzaju `` Calocybe '' jako `` Calocybe persicolor '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1991 r. Marcel Bon , przenosząc go do rodzaju `` Rugosomyces '' .
Niektóre synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. Po przeniesieniu do rodzaju `` Rugosomyces '' nazwa ta jest niespójna z nazwą naukową .
Morfologia .
