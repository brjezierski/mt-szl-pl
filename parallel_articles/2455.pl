Jaskrawiec pierścieniowaty
Jaskrawiec pierścieniowaty ( `` Caloplaca cirrochroa '' ( Ach . )
Th .
Fr . )
– gatunek grzybów z rodziny złotorostowatych ( `` Teloschistaceae '' ) .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Caloplaca , Teloschistaceae , Teloschistales , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1814 Erik Acharius nadając mu nazwę `` Lecanora cirrochroa '' .
