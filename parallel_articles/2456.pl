Czasznica miskowata
Czasznica miskowata ( `` Calvatia cyathiformis '' ( Bosc ) Morgan ) – gatunek grzybów należący do rodziny purchawkowatych ( `` Lycoperdaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Calvatia , Lycoperdaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy takson ten zdiagnozował w roku 1811 Louis Augustin Guillaume Bosc , nadając mu nazwę `` Lycoperdon cyathiforme '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1890 Andrew Price Morgan przenosząc go do rodzaju `` Calvatia '' .
Synonimy łacińskie :
Nazwę polską nadała Wanda Rudnicka-Jezierska w 1991 r .
Morfologia .
Duże , o średnicy 6–12 cm i wysokości do 10 cm , mniej więcej kulisty , lub odwrotnie gruszkowaty .
Posiada szeroką podstawę , z której wyrasta gruby sznur grzybniowy .
U młodych owocników egzoperydium białe , omszone lub szorstkie .
