Camagüey ( prowincja )
Camagüey – największa spośród czternastu kubańskich prowincji .
Jej stolicą jest Camagüey .
Teren prowincji jest równinny i położony na niewielkiej wysokości ponad poziomem morza .
Głównymi gałęziami gospodarki jest hodowla bydła i uprawa trzciny cukrowej .
Hoduje się również drób i uprawia ryż oraz cytrusy .
