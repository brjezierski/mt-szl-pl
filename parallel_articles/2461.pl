Zabytki .
W Cambrai znajdowała się niegdyś imponująca gotycka katedra .
Mierzyła ona 131 metrów długości , 72 m szerokości w transepcie .
Iglica katedry wznosiła się na wysokość 114 m. Została całkowicie zniszczona w czasie rewolucji francuskiej .
