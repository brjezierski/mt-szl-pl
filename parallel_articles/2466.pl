Camila Cabello
Camila Cabello , właśc .
Karla Camila Cabello Estrabao ( ur .
3 marca 1997 w Cojimar , Hawana ) – amerykańska piosenkarka i autorka tekstów pochodzenia kubańskiego i meksykańskiego .
Zyskała rozpoznawalność w lipcu 2012 jako jedna z wokalistek girlsbandu Fifth Harmony utworzonego podczas drugiej edycji amerykańskiej wersji programu `` The X Factor '' .
Podpisały kontrakt z wytwórniami Syco Music oraz Epic Records , które wydały ich dwa albumy studyjne .
Od 2016 artystka solowa , która początkowo śpiewała gościnnie w singlach innych wykonawców , takich jak „ Bad Things ” Machine Guna Kelly ’ ego , który uplasował się w pierwszej piątce listy `` Billboard '' Hot 100 .
Wydała jeden solowy album studyjny pt .
`` Camila '' ( 2018 ) , z którym osiągnęła sukces komercyjny , m.in .
docierając z nim do pierwszego miejsca listy `` Billboard 200 '' w Stanach Zjednoczonych .
Płytę promowała singlem „ Havana ” z gościnnym udziałem Younga Thuga , który stał się światowym hitem , docierając na szczyt list przebojów , takich jak np .
UK Singles Chart , Irish Singles Chart , Scottish Singles Chart , OLiS , ARIA Charts czy Hot 100 .
Laureatka statuetek muzycznych , takich jak Billboard Music Awards , American Music Awards , MTV Video Music Awards , MTV Europe Music Awards czy Teen Choice Awards .
Młodość .
Urodziła się 3 marca 1997 w Cojimar na Kubie .
Jej matką jest Kubanka Sinuhe Estrabao , a ojcem – Meksykanin Alejandro Cabello .
Ma siostrę o imieniu Sofia .
Początkowo mieszkała w Hawanie , następnie przeprowadziła się do Meksyku .
W wieku 5 lat zamieszkała w Miami .
Uczęszczała do szkoły średniej Palmetto w Miami , którą porzuciła w dziewiątej klasie w roku szkolnym 2012/13 dla kariery muzycznej .
Kariera zawodowa .
W 2012 wzięła udział w przesłuchaniach do programu `` The X Factor '' organizowanych w Greensboro w Karolinie Północnej .
Została wyeliminowana z programu na pierwszym etapie , lecz wkrótce jurorzy zaproponowali jej i czterem innym uczestniczkom stworzenie grupy , znanej później jako Fifth Harmony .
Zespół podpisał umowę z wytwórniami Syco Music oraz Epic Records .
