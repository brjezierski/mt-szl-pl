Campinas
Campinas ( wym .
[ ] ) – miasto w południowo-wschodniej Brazylii , we wschodniej części stanu São Paulo , położone 100 km na północ od São Paulo .
Według spisu ludności z 2010 r. Populacja miasta wynosi 1 080 999 , co czyni go czternastym najbardziej zaludnionym miastem Brazylii i trzecią pod względem liczby ludności gminą w stanie São Paulo .
Obszar metropolitalny miasta , Metropolitan Region of Campinas , obejmuje dwadzieścia gmin o łącznej liczbie 3665363 osób .
