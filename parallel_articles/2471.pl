Campobasso ( stacja kolejowa )
Campobasso ( włoski : `` Stazione di Campobasso '' ) – stacja kolejowa w Campobasso , w regionie Molise , we Włoszech .
Znajdują się tu 2 perony .
Stacja została otwarta wraz z linią do Benevento 21 września 1883 .
Według oceny dokonanej przez RIF , stacja znajduje się w kategorii `` srebrnej '' , która identyfikuje ją jako stacji o średniej frekwencji metropolii-regionalnych i długodystansowych .
