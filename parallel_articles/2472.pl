Lokalna kuchnia .
Dla Campora charakterystyczna jest kuchnia regionu Kalabrii , wywodząca się z kultury rolniczej i tradycji rybackiej .
Opiera się ona na prostych daniach , warstwowych ciastach i wieprzowinie .
