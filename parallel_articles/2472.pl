Campora San Giovanni
Campora San Giovanni – dzielnica miasta Amantea , w prowincji Cosenza we Włoszech .
Znajduje się blisko granicy prowincji Catanzaro .
Ekonomia .
Ekonomia Campora San Giovanni opiera się na rolnictwie oraz turystyce .
W latach 50 .
XX wieku rozpowszechniona została uprawa czerwonej cebuli Tropea , która obecnie jest dostępna na rynkach krajowych i zagranicznych .
Przez ostatnie 20 lat miasto jest w stanie szybkiego rozwój handlu i eksportu .
Jest to możliwe dzięki przechodzącej linii kolejowej , drodze SS18 , bliskości autostrady A3 ( 8 km ) oraz lotniska Lamezia Terme ( 25 km ) .
Geografia .
Campora jest usytuowana na wybrzeżu Morza Tyrreńskiego .
W latach 50 .
XX wieku zabudowa rozrosła się na klifie , doprowadzając do jej dominacji nad sąsiadującymi plażami .
Pobliskie wzgórza wykorzystywane są w uprawie winorośli oraz oliwek .
Z ich szczytów rozciąga się widok od zatoki Lamezia Terme , po wulkan Stromboli , którego dymy widoczne są w słoneczny dzień .
Niedaleko znajdują się również Wyspy Liparyjskie , na które można dostać się z przystani w mieście .
Historia .
Najstarszym budynkiem miasta jest XIV-wieczna wieża , nazywana w miejscowym dialekcie `` U Turriune '' .
Jej charakterystyczną cechą są zdobione konsole .
Niedawno Campora wzbogaciła się o przystań ( 2002 ) oraz plac Świętego Franciszka z Paoli .
Ten ostatni jest lokalnym patronem , a jego święto trwa od 1 do 3 września .
Charakterystyczną częścią uroczystości jest procesja , podczas której wzdłuż ulic przenoszona jest figura świętego .
