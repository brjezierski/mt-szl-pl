Canberra
Canberra ( wym .
lub ) – stolica Australii , miasto leżące w odległości 280 km od Sydney i 660 km od Melbourne w obrębie Australijskiego Terytorium Stołecznego .
Jest największym miastem Australii położonym w środku lądu i 8. co do wielkości miastem kraju .
Canberra ma 431 611 mieszkańców ( 2021 ) .
Miasto nie ma własnych władz miejskich , natomiast wszystkie decyzje dotyczące miasta podejmuje Zgromadzenie Ustawodawcze Australijskiego Terytorium Stołecznego .
Faktyczną władzę sprawuje Gubernator Australii .
Historia .
Przed europejskim osadnictwem teren dzisiejszej Canberry był sezonowo zamieszkany przez plemiona Ngunnawal i Walgalu .
Plemię Ngarigo żyło na południowym wschodzie tego terenu , Gundungurra na północy , Yuin na wybrzeżu , a Wiradjuri na zachodzie .
Odkrycia archeologiczne z obecnego regionu stołecznego wskazują obecność ludzi na tym terenie od 21 tys .
lat .
Słowo „ Canberra ” pochodzi z dialektu rodziny Ngabri , jednej z rodzin plemienia Ngunnawal , od słowa `` Kanbarra '' , znaczącego `` miejsce spotkań '' .
Nazwa ta była najwyraźniej używana jako odwołanie do corroboree odbywających się podczas sezonowych migracji ludzi z plemienia Ngunnawal w celu uczczenia ciem bogong ( `` Agrotis infusa '' ) , przelatujących przez region każdej wiosny .
Europejska kolonizacja rozpoczęła się na początku XIX wieku .
