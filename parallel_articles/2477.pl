Sandacz pospolity
Sandacz pospolity , sandacz ( `` Sander lucioperca '' ) – gatunek ryby okoniokształtnej z rodziny okoniowatych ( Percidae ) .
Zasięg występowania .
Europa od dorzecza Renu i Rodanu po Morze Kaspijskie oraz południowa Anglia .
Brak go w północnej Skandynawii , północnej Rosji oraz na półwyspach Apenińskim i Bałkańskim .
Występuje w jeziorach , zbiornikach zaporowych , średnich i dużych nizinnych rzekach , wyrobiskach oraz w płytkich wodach przybrzeżnych Bałtyku .
Preferuje głębokie , mętne wody o twardym , piaszczystym , żwirowatym bądź gliniastym dnie .
Jest wrażliwy na niedobór tlenu .
Charakterystyka .
Ciało wydłużone , lekko spłaszczone , wzdłuż linii bocznej 55–77 łusek .
Głowa stosunkowo mała , otwór gębowy przedni , sięgający za tylną krawędź oka , paszcza silnie uzębiona , kilka zębów w przedniej części szczęki znacznie większych od pozostałych .
Oczy duże , opalizujące , świecące w ciemnościach .
Płetwy grzbietowe dość daleko oddalone od siebie .
Grzbiet szarozielony lub szarobrązowy , boki jasne srebrzystozielone z 8–12 ciemnymi smugami często rozmazującymi się w plamy , brzuch biały .
Płetwy grzbietowe i ogonowa pokryte ciemnymi plamkami , rozmieszczonymi regularnie między promieniami .
