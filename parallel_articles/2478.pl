Candida boidinii
Candida boidinii C. Ramírez – gatunek grzybów zaliczany do klasy drożdżaków ( `` Saccharomycetes '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum :
`` Candida , Incertae sedis , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Ascomycota , Fungi '' .
Opisał go po raz pierwszy w 1953 r. Ramírez Gómez Carlos .
Synonimy :
Charakterystyka .
W Polsce wyizolowany został w ściekach kanalizacji komunalnej .
Gatunek ten występuje jednak pospolicie i został wyizolowany z różnych substratów związanych z działalnością człowieka ( fermentacja wina , produkcja oliwek , tepache itp . ) .
W naturalnym środowisku występuje w glebie , wodzie morskiej , sokach wielu gatunków drzew bogatych w cukier itp .
`` C. boidini '' ma duże zastosowanie w biotechnologii .
Wykorzystuje w swoim metabolizmie ksylozę .
Okazał się przydatny do badania genów związanych z degradacją metanolu .
Ponadto gatunek ten bierze udział w przetwórstwie oliwek , gdzie wykazuje różne wielofunkcyjne cechy , takie jak aktywność lipazy , tworzenie biofilmu na naskórku owoców oraz koagregację z gatunkami takimi jak `` Lactobacillus pentosus '' .
