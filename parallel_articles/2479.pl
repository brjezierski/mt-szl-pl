Candida glabrata
Candida glabrata ( H.W.Anderson ) S.A.Mey .
& amp ; Yarrow – gatunek grzybów zaliczany do rzędu drożdżaków ( `` Saccharomycetes '' ) .
Jest to grzyb bezotoczkowy , uważany za stosunkowo niepatogenny , komensalny , występujący w ludzkiej błonie śluzowej .
Może wywoływać zakażenia oportunistyczne , zwłaszcza u nosicieli wirusa HIV .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum :
`` Incertae sedis , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Ascomycota , Fungi '' .
Pierwotnie `` C. glabrata '' został sklasyfikowany w rodzaju `` Torulopsis . ''
Rodzaj ten został opisany w 1894 r. , a rodzaj `` Candida '' został nazwany dopiero w 1913 r. `` C. glabrata '' został pierwotnie umieszczony w rodzaju `` Torulopsis '' z powodu braku produkcji pseudostrzępek .
Jednak w 1978 r. ustalono , że zdolność do wytwarzania pseudostrzępek nie była wiarygodnym czynnikiem odróżniającym dla rodzaju `` Candida '' i zaproponowano , aby `` T. glabrata '' był klasyfikowany do rodzaju `` Candida '' .
`` C. glabrata '' jest znacznie bliżej spokrewniony z `` Saccharomyces cerevisiae '' niż z `` Candida albicans '' i należy do grupy gatunków , które duplikowały swój genom .
