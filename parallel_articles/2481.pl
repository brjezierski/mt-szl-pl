Candida parapsilosis
Candida parapsilosis ( Ashford ) Langeron & amp ; Talice — gatunek grzybów należący do rzędu `` Saccharomycetales '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Candida , Incertae sedis , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Saccharomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1928 roku Bailey Ashford , nadając mu nazwę `` Monilia parapsilosis '' .
Obecną , uznaną przez Index Fungorum , nazwę nadali mu w roku 1932 Maurice Langeron i R. V. Talice , przenosząc go jednocześnie do rodzaju `` Candida '' .
Istnieje ponad 20 synonimów jego nazwy .
Morfologia .
Hodowane na stałej pożywce YPD komórki `` C. tropicalis '' są owalne o wielkości 3–4 na 5–8 μm .
Hodowane przez trzy dni w temperaturze 25 °C na podłożu z mąki kukurydzianej z dodatkiem detergentu `` Tween 80 '' tworzą blastokonidia , pojedyncze lub w małych grupach , zebrane wzdłuż osi stosunkowo krótkiej pseudostrzępki .
Kolonie na płytkach YPD są okrągłe , płaskie lub lekko „ szpiczaste ” , stosunkowo małe ( 3–7 mm po 7 dniach ) , w kolorze kremowym , półmatowe lub lekko błyszczące o gładkiej lub pomarszczonej fakturze .
