Candida stellata
Candida stellata ( Kroemer & amp ; Krumbholz ) S.A. Mey .
& amp ; Yarrow – gatunek grzybów należący do rzędu `` Saccharomycetales '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Candida , incertae sedis , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Saccharomycotina , Ascomycota , Fungi '' .
Pierwotnie opisany przez Kroemera i Krumbholza w 1931 roku jako `` Saccharomyces stellatus '' , jego obecną systematykę ustalili w 1978 roku Yarrow i Meyer .
Niektóre synonimy nazwy naukowej :
Charakterystyka .
Gatunek występuje naturalnie na owocach winorośli ( `` Vitis sp . `` ) .
Przy produkcji wina dominuje on ( razem z `` Hanseniaspora uvarum '' ) pierwsze stadia fermentacji alkoholowej , która później jest kontynuowana przez `` Saccharomyces cerevisiae '' .
Według innych badań , po dokładnym sprawdzeniu taksonomii molekularnej kultur pozyskanych z winogron , sugeruje się , że kultury opisywane jako `` C. stellata '' należą do bardzo zbliżonego ( niemalże nieodróżnialnego ) gatunku : `` Candida zemplinina '' .
Gatunek ten , podobnie jak `` Candida stellata '' , posiada trzy chromosomy w komórce i cechuje go ich polimorfizm długości .
Genom `` Candida stellata '' jest jednak większy od genomu `` Candida zemplinina '' .
