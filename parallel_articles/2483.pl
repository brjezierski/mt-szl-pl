Candida tropicalis
Candida tropicalis ( Castell . )
Berkhout — gatunek grzybów należący do rzędu `` Saccharomycetales '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Candida , incertae sedis , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Saccharomycotina , Ascomycota , Fungi '' .
Po raz pierwszy , w 1910 roku , takson ten zdiagnozował Aldo Castellani , nadając mu nazwę `` Oidium tropicale '' .
Obecną , uznaną przez Index Fungorum .
nazwę nadał mu w roku 1923 C. M. Berkhout , przenosząc go do rodzaju `` Candida '' .
Ma ok. 100 synonimów nazwy naukowej .
Charakterystyka .
`` C. tropicalis '' jest drugim , po `` C. albicans '' , najbardziej wirulentnym grzybem z rodzaju `` Candida '' i , podobnie jak inne `` Candida '' , u człowieka i zwierząt może powodować zakażenia oportunistyczne ; szczególnie wirulentny jest wobec pacjentów cierpiących na białaczkę lub podobną chorobę .
Przebieg zakażenia i objawy są zbliżone do tych przy zakażeniu `` C. albicans '' i obejmują : zapalenie opon mózgowo-rdzeniowych , zapalenie wsierdzia , ostrą kandydozę ogólnoustrojową ( w tym kandydozę pochwy ) , odmiedniczkowe zapalenie nerek , zapalenie przełyku i pochwy u człowieka , a zapalenie sutków i poronienia grzybicze u zwierząt .
