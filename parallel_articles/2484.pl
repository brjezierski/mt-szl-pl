Candida viswanathii
Candida viswanathii Sandu & amp ; H.S .
Randhawa – gatunek grzybów należący do rzędu drożdżakowców ( `` Saccharomycetales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Candida , Incertae sedis , Saccharomycetales , Saccharomycetidae , Saccharomycetes , Saccharomycotina , Ascomycota , Fungi '' .
Jest to anamorfa `` Saccharomycetales '' .
Charakterystyka .
Po raz pierwszy wyizolowany z płynu mózgowo-rdzeniowego pacjenta zmarłego na zapalenie opon mózgowo-rdzeniowych , później także z plwociny podczas rutynowych badań próbek klinicznych .
W środowisku naturalnym był izolowany m.in .
ze skrzeli ryb z Oceanu Indyjskiego oraz z gleby w Południowej Afryce .
Grzyb miał działanie patogenne ( powodował śmierć ) , gdy był wstrzykiwany myszom laboratoryjnym , ale wyłącznie gdy podawano im jednocześnie kortyzon .
Dla myszy nietraktowanych tym lekiem grzyb był niegroźny .
Według analizy rRNA małej podjednostki rybosomu ( 18S ) , `` C. viswanathii '' razem z innymi przedstawicielami rodzaju `` Candida '' ( `` C. albicans '' , `` C. tropicalis '' i `` C. parapsilosis '' ) tworzy silnie wyodrębnioną od innych patogennych grzybów grupę .
Badania kariotypu i porównanie sekwencji DNA wykazały , że gatunek ten jest bardzo zbliżony do `` Candida lodderae '' .
Bez używania technik biologii molekularnej odróżnienie tych gatunków jest możliwe jedynie przez porównanie ich wzrostu w różnych warunkach : `` C. viswanathii '' w przeciwieństwie do `` C. lodderae '' nie może fermentować sacharozy , ale jest w stanie rosnąć w temperaturze 40 °C .
Pod względem pozostałych warunków hodowli są one identyczne .
Morfologia .
Kolonie hodowane przez trzy dni na stałej pożywce YPD są okrągłe , matowe , w kolorze kremowym o gładkiej lub pomarszczonej fakturze .
Pojedyncze komórki są owalne lub wydłużone , występują osobno lub pączkują często tworząc nieregularne , rozgałęzione łańcuchy wydłużonych komórek .
