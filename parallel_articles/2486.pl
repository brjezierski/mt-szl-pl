uproszczona : `` kan '' ; prowansalski `` Canas '' ) – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Nadmorskie na Lazurowym Wybrzeżu .
Obecnie miasto znane jest na całym świecie dzięki międzynarodowemu festiwalowi filmowemu i turystyce .
Historia i ludność .
Od czasów średniowiecza aż do początku XIX wieku Cannes było małą rolniczą i rybacką osadą .
W latach 30 .
XIX wieku zostało odkryte przez francuską i zagraniczną arystokrację , która zbudowała tutaj swoje rezydencje .
Szczególne zasługi dla rozwoju tego miasta oddał brytyjski polityk Lord Henry Peter Brougham ( 1778–1868 ) .
