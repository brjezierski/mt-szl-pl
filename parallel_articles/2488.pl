Canonsburg
Canonsburg – miasto w Stanach Zjednoczonych , w stanie Pensylwania , hrabstwo Nowy Jork .
Canonsburg położony jest ok. 29 km na południowy zachód od Pittsburga .
Miasto zostało założone przez płk .
John Canona w 1789 r. , prawa miejskie uzyskało w 1802 r. Canonsburg jest organizatorem dorocznego Oktoberfest oraz drugiej co do wielkości parady na Święto Niepodległości Stanów Zjednoczonych – corocznie jej przygotowania obserwuje prasa i telewizja .
Canonsburg popularny jest również dzięki firmie ANSYS , która ma tu swoją siedzibę .
