Kanossa
Kanossa ( wł .
`` Canossa '' ) – miejscowość i gmina we Włoszech , w regionie Emilia-Romania , w prowincji Reggio Emilia , u podnóża Apenin , ok. 25 km od Parmy .
Według danych na rok 2004 gminę zamieszkuje 3376 osób , 63,7 os./km² .
Zamek w Kanossie należał pierwotnie do Matyldy , hrabiny Toskanii .
„ Pójść do Kanossy ” .
Z Kanossą jest związany jeden z najsłynniejszych sporów w historii Europy .
Był to spór o inwestyturę między papieżem Grzegorzem VII a cesarzem rzymskim Henrykiem IV .
Spór toczył się o prawo do inwestytury duchownych .
Cesarz uważał , że to on powinien nadawać godności kościelne na ziemiach Cesarstwa , papież natomiast chciał zatrzymać ten przywilej w swoich rękach .
W końcu Henryk IV wypowiedział posłuszeństwo papieżowi , ten zaś , na synodzie w Rzymie w 1076 roku , ekskomunikował go .
Oznaczało to , że poddani cesarza zostali zwolnieni z nakazu posłuszeństwa względem władcy .
