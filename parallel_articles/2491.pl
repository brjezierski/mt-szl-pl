Pieprznik żyłkowany
Pieprznik żyłkowany ( `` Craterellus lutescens '' ( Fr . )
Fr . )
– gatunek grzybów z rodziny kolczakowatych ( `` Hydnaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Craterellus , Hydnaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1821 r. Elias Fries , nadając mu nazwę `` Canthrellus lutescens '' , w 1838 r. ten sam autor przeniósł go do rodzaju `` Craterellus '' .
Synonimów naukowych ma ok. 30 .
Niektóre z nich :
Polską nazwę nadał Władysław Wojewoda w 1999 r. , w polskim piśmiennictwie mykologicznym gatunek ten opisywany był również jako pieprznik żółtawy .
Obecnie nazwy te są niespójne z nazwą naukową , gdyż według Index Fungorum gatunek ten należy do rodzaju `` Craterellus '' ( lejkowiec ) .
