Cap-Haïtien ( hait .
`` Kap Ayisien '' ) – miasto w północnej części Haiti , stolica departamentu Nord .
Regionalny ośrodek przemysłu i przetwórstwa , ze względu na przeszłość i zabytki – cel turystyczny .
