Capalbio ( stacja kolejowa )
Capalbio ( wł .
`` Stazione di Capalbio '' ) – stacja kolejowa w Capalbio Scalo , w prowincji Grosseto , w regionie Toskania , we Włoszech .
Znajduje się na linii Piza – Rzym .
Położona jest około 9 km od centrum gminy Capalbio .
Według klasyfikacji RFI ma kategorię `` brązową '' .
Połączenia .
Stacja jest obsługiwana przez pociągi regionalne w kierunku Grosseto , Roma Termini i Pizy .
