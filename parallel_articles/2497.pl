Od XVI stulecia miasto kilkukrotnie zmieniało właściciela ( Wielka Brytania , Portugalia , Szwecja , Dania i Holandia ) .
Historia .
Założone przez Portugalczyków w XV wieku , miasto rozrastało się wokół zamku Cape Coast Castle , który teraz jest miejscem światowego dziedzictwa .
Zamienione w zamek przez Holendrów w 1637 , później rozwinięte przez Szwedów w 1652 i zdobyte przez Brytyjczyków w 1664 .
Brytyjczycy stąd kierowali swoimi działaniami na Złotym Wybrzeżu do chwili , kiedy Akra stała się stolicą w 1877 .
