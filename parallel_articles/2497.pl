Cape Coast
Cape Coast lub `` Cabo Corso '' ( pierwotnie znane jako `` Oguaa '' ) jest stolicą dystryktu i Regionu Centralnego Ghany , położone 165 km na zachód od Akry nad Zatoką Gwinejską .
Populacja według spisu w roku 2010 wynosiła 108,4 tys .
