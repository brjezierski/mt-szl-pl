.onion
.onion – pseudodomena najwyższego poziomu oznaczająca usługę osiągalną przez anonimową sieć Tor .
Takie adresy nie są tak naprawdę nazwami DNS , a .onion nie figuruje w rejestrze TLD , jednak – dzięki instalacji odpowiednich programów – przeglądarki i inne programy korzystające z internetu mogą wykorzystywać sieć Tor .
Nazwa onion odnosi się do trasowania cebulowego ( ang .
`` onion '' – cebula ) , wykorzystywanego w sieci Tor .
