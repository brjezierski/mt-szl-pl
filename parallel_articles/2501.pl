Caracas
Caracas ( pełna nazwa : Santiago de León de Caracas ) – stolica Wenezueli , największe miasto w kraju .
Geografia .
Położone na północy kraju , 11 km od wybrzeża Morza Karaibskiego , w dystrykcie stołecznym .
Liczba mieszkańców ( 2010 ) : 1966 tys .
Miasto cechuje policentryczny układ urbanistyczny , zabudowa rozciąga się wzdłuż głównej doliny oraz dolin bocznych od strony południowej .
W zachodniej części znajduje się historyczne centrum zbudowane na planie szachownicy z wieloma zabytkami architektury kolonialnej ( katedra , Casa Amarilla i in . )
oraz niskimi domami w stylu kolonialnym , częściowo zmodernizowanymi , które sąsiadują z nowoczesną , wysoką zabudową .
W otoczeniu ( w środkowej , wschodniej i południowej części miasta ) rozciągają się dzielnice mieszkaniowe , reprezentacyjne i handlowo-usługowe , z wysokimi biurowcami oraz dzielnice willowe .
Na stokach okolicznych gór i w wąwozach istnieją dzielnice nędzy , tzw .
`` ranchitos '' .
Pasmo Kordyliery Nadbrzeżnej , oddzielające miasto od morza , stanowi Park Narodowy El Ávila ( utworzony 1958 ) .
Historia .
Miasto zostało założone w 1567 przez hiszpańskiego odkrywcę , Diego de Losadę .
Początkowo nosiło nazwę `` Santiago de León de Caracas '' .
