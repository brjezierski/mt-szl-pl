Carcassonne
Carcassonne ( prowan .
`` Carcassona '' ) – miasto i gmina we Francji , w regionie Oksytania .
Miasto jest prefekturą departamentu Aude .
Przez gminę przepływa rzeka Aude .
Położone jest w Langwedocji , dawnej prowincji , przy drodze łączącej Pireneje z Masywem Centralnym .
Jest oddalone o 90 km na południowy wschód od Tuluzy .
Liczba mieszkańców w 2006 r. : 46 216 .
Według danych na rok 1990 gęstość zaludnienia wynosiła 668 osób/km² ( wśród 1545 gmin Langwedocji-Roussillon Carcassonne plasuje się na 6. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 24 .
