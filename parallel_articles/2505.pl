Cardiff ( wym .
[ ] ; wal .
`` Caerdydd '' , wym .
[ ] ) – miasto ze statusem `` city '' w Wielkiej Brytanii , stolica Walii , ośrodek administracyjny hrabstwa Cardiff , historycznie wchodzące w skład w hrabstwa Glamorgan ( Morgannwg ) .
Jest położone nad rzeką Taff , u jej ujścia do Kanału Bristolskiego .
W 2011 roku miasto liczyło 335 145 mieszkańców .
Powstało jako osiedle rzymskie z I-II wieku , później rozbudowane przez Normanów .
Znajduje się w historycznym hrabstwie Glamorgan , ale od roku 1996 stanowi osobne hrabstwo Cardiff .
Pierwotnie było to małe miasto , którego bardzo szybki rozwój , tak samo jak całego pobliskiego regionu , przypada na XIX wiek .
Miasto rozwinęło się dzięki transportowi węgla kamiennego z pobliskich kopalni .
Stolicą Walii został 20 grudnia 1955 r .
