Cargèse
Cargèse ( korsyk .
`` Carghjese '' ) to miejscowość i gmina we Francji , w regionie Korsyka , w departamencie Korsyka Południowa .
Według danych na rok 1998 gminę zamieszkiwały 982 osoby , a gęstość zaludnienia wynosiła 21 osób/km² .
