Carl Menger
Carl Menger von Wolfensgrün ( ur .
23 lutego 1840 w Nowym Sączu , zm .
27 lutego 1921 w Wiedniu ) – austriacki ekonomista , twórca austriackiej szkoły ekonomii .
Syn Antona i Karoliny z d. Gerzabek .
W roku 1871 opublikował `` Zasady ekonomii '' , których wydanie uznaje się za początek szkoły austriackiej .
Stworzył teorię wartości i ceny .
Opracował prakseologiczną metodologię badań .
Szczególną rolę w ekonomii odegrał jego wkład w rozwój koncepcji krańcowej użyteczności , dzięki której mógł odrzucić laborystyczną teorię wartości Adama Smitha i Davida Ricardo .
Pracę Mengera kontynuowali jego uczniowie , z których najbardziej znani to Eugen von Böhm-Bawerk i Friedrich von Wieser .
Życiorys .
Menger urodził się w Nowym Sączu ( Galicja ) , pod ówczesnym zaborem austriackim .
Wywodził się z drobnej , lecz względnie zamożnej szlachty .
Jego ojciec Anton był prawnikiem , a matka Caroline córką bogatego czeskiego kupca .
Miał dwóch braci : Antona i Maxa , obaj zostali znanymi prawnikami .
Po ukończeniu gimnazjum studiował prawo na uniwersytetach w Pradze i Wiedniu , a następnie uzyskał doktorat z prawa na Uniwersytecie Jagiellońskim .
W latach 60 .
XIX wieku Menger skończył studia i podjął pracę jako dziennikarz relacjonujący i analizujący wydarzenia rynkowe , początkowo dla „ Lemberger Zeitung ” we Lwowie , a następnie dla „ Wiener Zeitung ” w Wiedniu .
