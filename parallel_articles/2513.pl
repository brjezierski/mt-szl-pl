Carlsberg ( przedsiębiorstwo )
Carlsberg A/S – największe przedsiębiorstwo piwowarskie w Danii i jedno z największych na świecie .
Swoje zakłady posiada w ponad 50 krajach , zatrudniając łącznie około 43 tys .
pracowników .
Siedziba przedsiębiorstwa znajduje się w Kopenhadze .
Carlsberg posiada większościowe udziały w kilku ważnych europejskich browarach , takich jak Carlsberg UK ( Wielka Brytania ) , Carlsberg Sweden ( Szwecja ) , Ringnes ( Norwegia ) , Sinebrychoff ( Finlandia ) , Holsten-Brauerei AG ( Niemcy ) , Browar Žatec ( Czechy ) czy Carlsberg Polska .
Należący do Grupy Carlsberga Baltic Beverages Holding działa na rynkach Rosji , Ukrainy i krajów nadbałtyckich .
Liczne działania grupy w Azji koordynuje Carlsberg Asia .
Do najważniejszych marek grupy Carlsberg na świecie możemy zaliczyć : Carlsberg , Holsten Pilsener , Okocim , Saku , Aldaris , Tuborg , Karhu , Carls , Jacobsen , Baltika , Kasztelan .
Przedsiębiorstwo zostało założone w kopenhaskiej dzielnicy Valby w 1847 r. przez J.C. Jacobsena .
Początkowo browar nosił nazwę `` Carlsberg-Bryggerier København '' , a samo piwo warzone było według receptury bawarskiej .
