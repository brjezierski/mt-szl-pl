Carrie
Carrie – pierwsza powieść Stephena Kinga , wydana w 1974 roku .
Opowiada o nieśmiałej i odrzuconej przez towarzystwo licealistce posiadającej zdolności telekinetyczne .
Powieść zapewniła początkującemu pisarzowi rozgłos i opinię autora tworzącego głównie horrory .
Charakterystyczna głównie ze względu na styl ; duża część książki napisana jest jako powieść epistolarna , prezentując listy , wycinki z gazet , artykuły i fragmenty książek .
Wielokrotnie wydawana w Polsce , nakładem wydawnictwa Iskry ( 1990 ) , Prima ( 1996 , 1998 ) oraz Prószyński i S-ka ( 2003 , 2004 , 2007 ) .
Opis fabuły .
Główną bohaterką powieści jest młoda dziewczyna – Carrie White .
Wychowywana jest przez samotną matkę – fanatyczkę religijną .
W szkole jest nielubiana , inni młodzi ludzie dokuczają jej .
Wraz z pierwszą miesiączką , dziewczyna odkrywa w sobie moce telekinetyczne ( ma między innymi możliwość przesuwania przedmiotów za pomocą myśli , może częściowo kontrolować umysły innych osób ) .
Za ich pomocą zaczyna szukać zemsty na prześladowcach .
Ekranizacje .
Książka doczekała się kilku ekranizacji .
W 1976 roku Brian De Palma zrealizował kinową adaptację powieści .
Stała się ona klasyką kina grozy , a tytułową rolę zagrała Sissy Spacek .
W filmie zadebiutował również John Travolta .
Film doczekał się kontynuacji ( `` '' ) w 1999 roku .
Drugiej ekranizacji , tym razem telewizyjnej , dokonał w 2002 roku David Carson .
Film początkowo miał być pilotem serialu , ale do realizacji kolejnych odcinków nie doszło i stał się samodzielną produkcją .
Na podstawie książki powstał także broadwayowski musical .
W 2013 nakręcono trzecią ekranizację powieści , wyreżyserowaną przez Kimberly Peirce .
W rolach głównych wystąpiły Chloë Grace Moretz jako Carrie White oraz Julianne Moore jako Margaret White .
Film wszedł do kin w Stanach Zjednoczonych pod koniec września , a w Polsce 18 października 2013 , w dystrybucji Forum Film Poland .
