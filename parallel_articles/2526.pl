Cartagena de Indias
Cartagena de Indias – miasto w północnej Kolumbii , nad Morzem Karaibskim .
Jest stolicą departamentu Bolívar .
Według spisu ludności z 30 czerwca 2018 roku miasto liczyło 876 885 mieszkańców , co czyniło je trzecim pod względem wielkości populacji miastem kraju .
Historia .
Miasto zostało założone w 1532 roku przez Pedra de Heredię .
