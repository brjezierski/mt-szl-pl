Cascais
Cascais – miejscowość w Portugalii , leżąca w dystrykcie Lizbona , w regionie Lizbona w podregionie Grande Lizbona nad Oceanem Atlantyckim .
Miejscowość jest siedzibą gminy o tej samej nazwie .
Krótki opis .
Położona jest około 30 kilometrów na zachód od Lizbony i . Cascais było pierwotnie miasteczkiem rybackim , jednak pod koniec XIX wieku stało się znane jako miejsce wypoczynku portugalskiej rodziny królewskiej .
Podczas II wojny światowej Portugalia była neutralna i wiele innych europejskich rodzin królewskich , m.in .
włoska , hiszpańska i bułgarska , osiadło w Cascais .
Miasto ma duży port jachtowy i kilka kilometrów plaż .
