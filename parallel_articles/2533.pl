Caserta ( stacja kolejowa )
Caserta ( wł : `` Stazione di Caserta '' ) – stacja kolejowa w Casercie , w regionie Kampania , we Włoszech .
Znajdują się tu 3 perony .
Stacja została otwarta 20 grudnia 1843 .
Została ona zbudowana naprzeciwko Pałacu Królewskiego , aby umożliwić łatwiejsze dotarcie do niego .
Od samego początku odgrywała kluczową rolę zarówno w transporcie lokalnym i krajowym , zarówno dla ruchu pasażerskiego i towarowego .
Stacja jest częścią projektu Centostazioni .
Rocznie z usług stacji korzysta 5,4 mln pasażerów .
