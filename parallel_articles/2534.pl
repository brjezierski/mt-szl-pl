Casio
– międzynarodowe przedsiębiorstwo branży elektronicznej , z siedzibą w dzielnicy Shibuya w Tokio , Japonia .
Firmę Casio założył w kwietniu 1946 r. Tadao Kashio .
W 1957 roku dotychczasowa nazwa Kashio Seisakujo została zmieniona na Casio Computer Co. Ltd. , a do zarządu przedsiębiorstwa dołączyli pozostali bracia Kashio : Kazuo , Toshio i Yukio .
Nazwa przedsiębiorstwa pochodzi od nazwiska prezesa zapisanego w rōmaji i jest dopasowana dla zagranicznego klienta .
Zmiana nazwy z Kashio na Casio była tłumaczona możliwością uznania koncernu za firmę włoską , czego Tadao Kashio chciał uniknąć .
