Castelfranco Veneto ( stacja kolejowa )
Castelfranco Veneto ( włoski : `` Stazione di Castelfranco Veneto '' ) – stacja kolejowa w Castelfranco Veneto , w prowincji Treviso , w regionie Wenecja Euganejska , we Włoszech .
Jest stacja węzłową na liniach Calalzo – Padewa , Trydent – Wenecja i Vicenza – Treviso .
Infrastruktura stacyjna jest zarządzana przez Rete Ferroviaria Italiana ( RFI ) , natomiast budynkiem dworca zarządza Centostazioni , wchodzącej w skład Ferrovie dello Stato .
Historia .
W dniu 15 lipca 1908 stacja znalazła się na linii Mestre-Bassano , zarządzanej przez SIFV , która zarządzała stacją Castelfranco .
Następnie zarządzaniem stacji zostało podzielone na dwie firmy wchodzące w skład FS .
Infrastruktura .
Stacja kolejowa posiada duży , trzypiętrowy budynek pasażerski .
Na obsługę pasażerów składają się : kasy , poczekalnia jak również bar , znajdujące się na parterze budynku .
Na infrastrukturę torową składają się 4 perony i 7 torów , połączonych ze sobą przejściem podziemnym .
Wszystkie perony posiadają wiaty peronowe .
