Castellet-lès-Sausses
Castellet-lès-Sausses – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 119 osób , a gęstość zaludnienia wynosiła 2 osoby/km² .
W styczniu 2015 r. Castellet-lès-Sausses zamieszkiwały 124 osoby , przy gęstości zaludnienia wynoszącej 2,1 osób/km² .
