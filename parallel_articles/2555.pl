Katania
Katania ( wł .
`` Catania '' ) – miasto i port we Włoszech , w regionie Sycylia , w prowincji Katania , u podnóża Etny .
Według danych na rok 2004 gminę zamieszkiwały 306 464 osoby ( 1702,6 os./km² ) .
Geografia .
Katania leży na wschodnim wybrzeżu Sycylii na północnym krańcu Zatoki Katańskiej , u podnóża wulkanu Etna .
