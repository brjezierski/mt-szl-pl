Catanzaro ( stacja kolejowa )
Catanzaro ( wł .
`` Stazione di Catanzaro '' ) – stacja kolejowa w Catanzaro , w prowincji Catanzaro , w regionie Kalabria , we Włoszech .
Położona jest na linii Lamezia Terme – Catanzaro Lido .
Znajduje się w dzielnicy Germaneto , kilka kilometrów od centrum miasta .
Według klasyfikacji RFI ma kategorię `` srebrną '' .
Historia .
Stacja kolejowa w Catanzaro został uruchomiona 15 czerwca 2008 , równocześnie z otwarciem nowej linii między Settingiano i Catanzaro Lido .
Zastąpiła ona starą stację Catanzaro , położoną w dzielnicy Sala u stóp wzgórza , na którym znajduje się centrum starego miasta .
