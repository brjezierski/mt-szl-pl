Cavaillon ( stolica tytularna )
Cavaillon ( łac .
`` Cavallicensis '' ) – stolica historycznej diecezji we Francji erygowanej w roku 400 , a włączonej w 1801 w skład archidiecezja Awinionu .
Współcześnie miasto Cavaillon znajduje się w regionie Prowansja-Alpy-Lazurowe Wybrzeże we Francji .
Obecnie katolickie biskupstwo tytularne ustanowione w 2009 przez papieża Benedykta XVI .
