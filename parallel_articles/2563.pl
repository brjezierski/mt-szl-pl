Ćottogram
Ćottogram , dawniej Ćittagong , Czittagong , Czatgano ( ben .
`` চট্টগ্রাম '' , ang .
`` Chittagong '' ) – miasto w południowo-wschodnim Bangladeszu , w pobliżu ujścia rzeki Karnaphuli do Zatoki Bengalskiej .
Znaczenie .
Ośrodek administracyjny prowincji Ćottogram .
Miasto liczy ok. 3,6 mln mieszkańców ( 2008 ) – drugie co do wielkości miasto kraju .
Historia .
W VII wieku miejscowość nazywano `` śpiącą pięknością wynurzającą się z mgieł i wody '' .
W XVI wieku Portugalczycy nazwali ją Porto Grande .
W przeciągu historii miasto podlegało m.in .
Sułtanatowi Dehlijskiemu , Wielkim Mogołom , Portugalii i Wielkiej Brytanii ( od 1766 ) .
