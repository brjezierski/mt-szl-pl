Cebu City
Cebu City – miasto na Filipinach na wschodnim wybrzeżu wyspy Cebu .
Ośrodek administracyjny prowincji Cebu .
W 2010 roku jego populacja liczyła 866 171 mieszkańców .
Jest piątym najludniejszym miastem kraju .
Obszar metropolitalny Cebu ( zwany Metro Cebu ) obejmujący m.in .
miasta : Cebu , Talisay , Consolacion , Mandaue , Lapu-Lapu jest drugim co do wielkości na Filipinach po Metro Manila .
Ośrodek przemysłu spożywczego , petrochemicznego , włókienniczego , drzewnego .
Główny port morski wyspy , obsługuje wywóz manili , kopry , drewna i oleju kokosowego .
Światowe centrum produkcji mebli rattanowych .
W mieście funkcjonuje 5 wyższych uczelni , najstarszą z nich jest katolicki uniwersytet założony przez Jezuitów w 1595 roku – Uniwersytet Świętego Karola .
Historia .
7 kwietnia 1521 roku przybył tu Ferdynand Magellan ze swoją wyprawą ; w miejscu lądowania ustawił krzyż , który przetrwał do dziś i znajduje się w małej kapliczce na ulicy noszącej dziś imię Magellana ; zginął 27 kwietnia 1521 na pobliskiej wysepce Mactan .
Osadę udało się założyć Hiszpanom w 1565 roku , nazwano ją San Miguel to Villa del Santissimo Nombre de Jesus ; w tymże roku ufundowano pierwszą świątynię katolicką , Basilica Minore del Santo Niño ; w 1571 roku miasto przemianowano na Cebu .
