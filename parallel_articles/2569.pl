Cehu Silvaniei
Cehu Silvaniei ( węg .
`` Szilágycseh '' ) – miasto w północno-zachodniej Rumunii , w okręgu Sălaj ( Siedmiogród ) .
Liczy 8468 mieszkańców ( dane na rok 2004 ) .
Miasto znane jest ze starego cmentarza żydowskiego .
Merem miasta od 2004 jest Vasile Cuceu , członek Sił Demokratycznych .
