Celebes
Celebes , Sulawesi – indonezyjska wyspa na Oceanie Spokojnym wchodząca w skład Archipelagu Malajskiego i Wielkich Wysp Sundajskich .
Z powierzchnią równą 174 600 km² znajduje się na 11. miejscu listy największych wysp świata .
Celebes ma nieregularny , rozczłonkowany kształt – stanowiące dużą część wyspy cztery półwyspy oddzielone są głęboko wcinającymi się zatokami .
Wyspa ta nie ma nizin przybrzeżnych .
Wyspę opływają morza : Flores , Banda , Moluckie , Celebes .
Terytorium Celebesu jest górzyste , występują liczne wulkany – zarówno aktywne jak i wygasłe .
Najwyższym z nich jest Soputan ( 1827 m n.p.m . ) .
W centrum wyspy znajdują się Góry Molengraffa , z najwyższym szczytem jest Rantekombola 3455 m n.p.m .
( według innych źródeł jest nim Rantemario 3478 m n.p.m . ) .
W dolinach śródgórskich i rowach tektonicznych oddzielających poszczególne pasma leżą liczne jeziora .
Klimat równikowy wybitnie wilgotny , średnia roczna suma opadów : 2000 mm .
Cechuje się nierównomiernym rozkładem opadów w ciągu roku .
Wyspa pokryta jest wiecznie zielonymi lasami równikowymi .
W kotlinach śródgórskich ukształtowała się formacja przypominająca sawannę .
Uprawia się głównie palmę kokosową , ryż , kawę oraz kauczukowiec .
Na wyspie eksploatuje się rudy niklu i żelaza oraz szlachetne odmiany drewna .
