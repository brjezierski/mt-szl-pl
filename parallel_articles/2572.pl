Cellule
Cellule – miejscowość i dawna gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Puy-de-Dôme .
W 2013 roku jej populacja wynosiła 1171 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Cellule oraz La Moutade – utworzono nową gminę Chambaron-sur-Morge .
Siedzibą gminy została miejscowość La Moutade .
