Krzyż celtycki
Krzyż celtycki – forma krzyża , od wieków znana wyznawcom religii przedchrześcijańskich , w którym czteroramienny krzyż umieszczony jest w okręgu , symbolizującym celtyckie wianki , tzw .
ruta .
Współcześnie ( mimo swego przedchrześcijańskiego pochodzenia ) krzyż celtycki jest jedną z form krzyża akceptowanego przez Kościół katolicki jako religijny symbol chrześcijan .
Przede wszystkim stał się on jednak charakterystyczny dla celtyckiego chrześcijaństwa .
Mimo to jest również spotykany w ofercie tzw .
sklepów ezoterycznych jako amulet lub talizman .
Warianty .
Krzyż celtycki występuje najczęściej w wersji długiej i krótkiej :
Rzeźby .
W celtyckich regionach Wysp Brytyjskich wolno stojące wysokie krzyże ( ang .
`` high cross '' ) zaczęto stawiać co najmniej w VIII wieku .
Niektóre z nich noszą na sobie runiczne inskrypcje .
Te wolno stojące krzyże przetrwały w Kornwalii i Walii , na wyspie Iona i na Hebrydach oraz w Irlandii .
Inne kamienne rzeźby w tym kształcie można znaleźć w Kumbrii i na pograniczu Szkocji , choć część z nich stawiana była według anglosaskiej , a nie celtyckiej tradycji .
Najsłynniejsze `` wysokie '' krzyże to Cross of Kells w hrabstwie Meath oraz krzyże w Monasterboice , w hrabstwie Louth .
Pochodzenie .
Krzyż umieszczony w okręgu zwany też krzyżem słonecznym od najdawniejszych czasów symbolizował słońce .
Koło z dwiema przeciętymi osiami to jeden z najstarszych symboli solarnych Indoeuropejczyków , występujący zresztą nie tylko u Celtów , motyw pojawia się np .
wśród Słowian wschodnich .
W północno-zachodniej Europie czasów pogańskich stał się atrybutem nordyckiego boga Odyna , inaczej Wodana .
Kształt tego symbolu przejęli chrześcijanie zamieszkujący Brytanię i Irlandię .
Chrześcijańska wersja krzyża celtyckiego została trochę zmodyfikowana - ma wystające poza okrąg wszystkie ramiona i bardziej wydłużone dolne ramię stanowiące podstawę , dzięki czemu przypomina w większym stopniu klasyczny krzyż chrześcijański ( łaciński ) .
Staroangielskim słowem oznaczającym „ krzyż ” jest „ rood ” .
