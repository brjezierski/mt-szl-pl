Podstawkorożek zbożowy
Podstawkorożek zbożowy ( `` Ceratobasidium cereale '' D.I .
Murray & amp ; Burpee ) – gatunek grzybów z typu podstawczaków ( `` Basidiomycota '' ) .
Grzyb mikroskopijny , który u zbóż wywołuje ostrą plamistość oczkową .
Choroba ta występuje w Polsce .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ceratobasidium , Ceratobasidiaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisali go w 1977 r. D.I .
Murray i L.L Burpee na liściach pszenicy zwyczajnej , nadając mu nazwę `` Rhizoctonia ceralis '' .
Wówczas znany był tylko jako anamorfa .
W 1984 r. ci sami mykolodzy wyizolowali także teleomorfę , której nadali nazwę `` Ceratobasidium cereale '' .
