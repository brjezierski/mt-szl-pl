Podstawkorożek szarawy
Podstawkorożek szarawy ( `` Ceratobasidium cornigerum '' ( Bourdot ) D.P .
Rogers ) – gatunek grzybów z typu podstawczaków ( `` Basidiomycota '' ) .
Grzyb mikroskopijny , będący saprotrofem i grzybem tworzącym mykoryzę ze storczykami , ale również fakultatywnym patogenem roślinnym , powodującym wiele ważnych gospodarczo chorób roślin uprawnych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ceratobasidium , Ceratobasidiaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1922 r. Hubert Bourdot , nadając mu nazwę `` Corticium cornigerum '' .
Wówczas znany był tylko jako anamorfa .
