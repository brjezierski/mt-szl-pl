Ceratocystis fimbriata
Ceratocystis fimbriata Ellis & amp ; Halst .
– gatunek grzybów workowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ceratocystidaceae , Microascales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go Job Bicknell Ellis i Byron David Halsted w 1890 r. na bulwach batatów w New Jersey .
Synonimy :
Morfologia .
Perytecja na powierzchowni , częściowo lub całkowicie zanurzone w tkankach żywiciela , o barwie od brązowej do czarnej , kuliste , o średnicy 140–220 µm i gładkiej powoerzchni , z długą , frędzelkowato zakończoną szyjką , czarną , bladobrązową do szklistej w kierunku wierzchołka , lekko zwężającą się , o długości do 900 µm .
W ostioli strzępki szkliste , wyprostowane lub umiarkowanie zgięte .
Askospory eliptyczne , z galaretowatą otoczką i kapturkiem , szkliste , nieseptowane , gładkie , 4,5–8 × 2,5–5,5 µm .
Konidiofory smukłe , wyrastające bocznie ze strzępek , z przegrodami , fialidowe , od szklistych do bardzo bladobrązowych , o długości do 160 µm , zwykle zwężające się ku końcowi i tworzące ciąg konidiów przez otwarty koniec .
Konidia cylindryczne , ścięte na końcach , szkliste , gładkie , 11–25 ( –15 ) x 4–5,5 µm .
Chlamydospory odwrotnie jajowate do owalnych , grubościenne , brązowe , 9–18 × 6–13 µm .
Występowanie .
Mikroskopijny grzyb pasożytniczy , polifag rozprzestrzeniony na całym świecie .
Występuje na wielu gatunkach roślin , wśród roślin uprawnych m.in .
na kokosach , kawowcu , kauczukowcu , batatach , śliwach , kakaowcu , strączyńcu cewiastym , mangu indyjskim .
Występuje także na `` Crotalaria juncea '' , `` Crotolaria retusa '' , `` Gliricidia septum '' , korzenniku lekarskim , topoli białej , topoli osice , dębach .
Wywołuje u nich choroby roślin .
Występuje również w Polsce na topolach , na których wywołuje chorobę o nazwie rak tarczowaty topoli .
W 1984 r. K. Przybył opisała jego występowanie na topolach w Arboretum w Kórniku .
W rozprzestrzenianiu go główną rolę odgrywa wiatr i deszcz .
Stwierdzono też rozprzestrzenianie tego patogenu wraz z odchodami owadów niektórych zwierząt .
Wśród zwierząt jego wektorami są : `` Hypothenemus plumeriae '' , `` Melanotus caudex '' , `` Platypus '' , `` Steirastoma brevis '' , `` Xyleborus '' i mysz polna .
