Cercedilla
Cercedilla – miejscowość w Hiszpanii we wspólnocie autonomicznej Madryt , 57 km na północny zachód od Madrytu .
Otoczone pasmem górskim Sierra de Guadarrama .
Nieopodal znajduje się słynny hiszpański stok narciarski Puerto de Navacerrada .
To miasto rodzinne Francisco Fernández Ochoa ( 1950-2006 ) , alpejskiego zawodnika , znanego z tego , że jest pierwszym ( i jedynym ) Hiszpanem , który zdobył złoty medal na Zimowych Igrzyskach Olimpijskich .
