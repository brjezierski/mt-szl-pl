Cercospora apii
Cercospora apii Fresen .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Wywołuje chorobę o nazwie chwościk selera .
Gatunek kosmopolityczny , występujący poza Antarktydą na wszystkich kontynentach , a także na wielu wyspach .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cercospora , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Znana jest tylko anamorfa .
Jeżeli występuje teleomorfa , to należy do rodzaju `` Mycosphaerella '' .
`` Cercospora apii '' po raz pierwszy opisany został przez Freseniusa w 1863 r. , ale w ostatnich latach wykonano wiele badań molekularnych i genetycznych w obrębie rodzaju `` Cercospora '' .
Skomplikowały one jego taksonomię .
Wyniki badań wskazują , że bardzo liczne taksony opisane jako odrębne gatunki , to w istocie synonimy .
W 2003 r. Crous i Brown utworzyli gatunek zbiorowy `` Cercospora apii sensu lato '' , do którego zaliczyli 281 morfologicznie nieodróżnialnych taksonów – w tym również `` Cercospora beticola '' wywołującego chorobę chwościk buraka .
Sprawę komplikuje dodatkowo fakt , że chorobę chwościk selera wywołuje także `` Cercospora apiicola '' .
