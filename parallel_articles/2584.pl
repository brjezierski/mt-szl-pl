Cercospora beticola
Cercospora beticola Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cercospora , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Anamorfa : `` Mycosphaerella '' .
Synonimy :
Morfologia i fizjologia .
Grzyb mikroskopijny .
Na liściach zaatakowanej rośliny tworzy skupiska konidioforów wytwarzających duże ilości zarodników konidialnych .
Konidiofory nierozgałęzione , proste lub nieco zakrzywione , jasnobrązowe , o wysokości 30–105 μm i szerokości 2,5 μm .
Konidia oddzielają się pojedynczo , są wrzecionowate lub jajowate , proste lub lekko zakrzywione , bezbarwne , gładkie .
Mają ( 4– ) 6–10 ( –16 ) przegród , długość 40–160 μm , szerokość 3,5–4 μm i pogrubioną bliznę u nasady .
Endobiont żyjący wewnątrz tkanek rośliny , pomiędzy jej komórkami .
Na powierzchni rośliny widoczne są tylko wywołane przez niego oznaki etiologiczne ( barwne plamy , itp .
) .
Występowanie i siedlisko .
Występuje w Afryce ( Etiopia , Kenia , Libia , Malawi , Mauritius , Nigeria , Sierra Leone , Sudan , Tanzania , Togo , Uganda , Zambia , Zimbabwe ) , Azji ( Hongkong , Indie , Malezja , Nepal , Pakistan , Iran , Irak , Izrael , Jordania , Oman , Turcja , Cypr , Borneo ) , Oceanii ( Papua-Nowa Gwinea ) , Europie ( Czechy , Słowacja , Anglia , Francja , Niemcy , Włochy , Malta , Polska , Rumunia , Szwajcaria ) , Ameryce Północnej ( USA ) , Ameryce Południowej ( Wenezuela ) , Ameryce Środkowej ( Antigua , Kuba , Jamajka ) .
Rozwija się na następujących gatunkach roślin : `` Beta ciclae '' , `` Beta maritima '' , `` Beta putternulaei '' , `` Beta trigyna '' , `` Beta vulgaris '' , `` Chenopodium ambrosioides '' , `` Chenopodium beticola '' , `` Chenopodium murale '' , `` Chenopodium polyspermi , '' `` Chenopodium urbicum '' , `` Spinacia oleracea '' .
Znaczenie .
Pasożyt niektórych gatunków roślin .
Uważa się , że u buraków wywołuje chorobę o nazwie chwościk buraka .
Biorąc jednak pod uwagę , że między wieloma gatunkami `` Cercospora '' brak istotnych różnic morfologicznych , a są one polifagami , nie jest to pewne .
Nawet badaniami molekularnymi ( badanie sekwencji regionów ITS ) nie udało się jednoznacznie ustalić który z gatunków `` Cercospora '' wywołuje tę chorobę .
