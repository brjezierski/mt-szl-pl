Cercosporella primulae
Cercosporella primulae Allesch .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cercosporella , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Według Index Fungorum jednak obydwa podane powyżej gatunki to taksony niepewne .
Morfologia i fizjologia .
Grzyb mikroskopijny , endobiont żyjący wewnątrz tkanek rośliny , pomiędzy jej komórkami .
Na liściach zaatakowanej rośliny tworzy tylko oznaki etiologiczne .
Są to plamy widoczne na obydwu stronach blaszki liściowej .
Mają barwę od żółtawej przez ochrową do brązowej .
Grzybnia wewnętrzna , strzępki bezbarwne z przegrodami , o szerokości 2–5 μm .
Konidiofory w małych grupkach , rzadko samotne , wyrastające z grzybni poprzez szparki liścia , lub przebijające jego naskórek .
Są bezbarwne , wyprostowane , proste do kolankowatych-krętych , o długości 10–50 μm i szerokości 2,5–7 μm .
