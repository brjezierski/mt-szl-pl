Cerkiew Kazańskiej Ikony Matki Bożej w Augustowie
Cerkiew Kazańskiej Ikony Matki Bożej – cerkiew prawosławna w Augustowie , powstała w 1569 , rozebrana po 1938 .
Historia .
Pierwsza wzmianka o parafii prawosławnej w Augustowie pochodzi z 1553 .
Drewniana cerkiew pod wezwaniem Matki Bożej w Augustowie została ufundowana przez króla Zygmunta Augusta w 1569 .
Była położona nad brzegiem Netty , przy ulicach Cerkiewnej i Koziej .
Po 1596 została przekazana unitom i pozostawała w ich rękach do likwidacji Kościoła unickiego w Imperium Rosyjskim w 1875 .
Był to prosty kwadratowy budynek z dwuspadowym dachem .
Odróżniał się od innych tym , że nie miał komina , a nad wejściem była mała kwadratowa kopuła z małym krzyżem i dzwonem .
W 1879 została poddana przebudowie według projektu Zaleskiego .
Początkowo maksymalnie mogła pomieścić 44 osoby , po przebudowie – ok. 300 osób .
Pracami , które trwały od grudnia 1878 do lutego 1879 i kosztowały 500 rubli , zajął się ówczesny proboszcz , ks .
Maksymilian Stępkowski .
Pieniądze zostały pozyskane z Banku Polskiego .
Władze Państwowe wpłaciły do Banku na rzecz parafii w 1839 r. 180 rubli srebrnych za oddaną część ogrodu pod budowę Kanału Augustowskiego .
Z procentów w 1878 było już 700 rubli .
Cerkiew została poświęcona 4 lutego 1879 .
Nadano jej wezwanie Kazańskiej Ikony Matki Bożej .
Ceremonii konsekracji przewodniczył dziekan augustowski ks .
Jan Jarmołowicz .
W ramach rozbudowy do cerkwi dobudowano część ołtarzową , powiększono przedsionek , wzniesiono bizantyjską kopułę nad nawą , zaś nad przedsionkiem umieszczono dzwonnicę z kopułą .
Cerkiew w nowej postaci była wzniesiona na planie krzyża .
Dach budynku był kryty gontem i ozdobiony pięcioma krzyżami .
W 1914 cerkiew została porzucona z powodu wyjazdu prawosławnych z Augustowa ( bieżeństwo ) .
Zaniedbany obiekt został zaadaptowany przez Niemców na prowizoryczną kuźnię .
Po 1918 cerkiew została zamknięta .
Zdewastowany budynek został rozebrany po 1938 , zaś w 1941 splantowano górkę , na której stała świątynia ( obecnie znajduje się tam boisko przy Gimnazjum nr 1 ) .
Architektura .
Cerkiew w Augustowie była wzniesiona na planie kwadratu , nakryta dachem dwuspadowym , nie posiadała kopuły , jedynie zwieńczoną cebulastym hełmem sygnaturkę nad nawą .
Była trójdzielna , w całości wzniesiona z drewna .
Dzwonnica cerkwi , z jednym dzwonem , znajdowała się nad przedsionkiem .
