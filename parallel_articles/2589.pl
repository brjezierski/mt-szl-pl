Cerkiew św .
Aleksandra Newskiego ( ros .
Церковь Александра Невского в Александруве Куявском ) – nieistniejąca cerkiew prawosławna w Aleksandrowie Kujawskim , wzniesiona w 1877 i rozebrana w okresie międzywojennym .
Historia .
W 1860 została zbudowana linia kolejowa z Łowicza do granicy prusko-rosyjskiej , która miała być częścią dłuższej linii łączącej Imperium Rosyjskie z Prusami .
Na terenie ówczesnego folwarku Białe Błota wybudowano dworzec kolejowy .
W tym samym obiekcie znalazła się również komora celna , stacja telegrafu , urząd pocztowy oraz biuro ruchu kolejowego .
W początkowym etapie istnienia dworca i komory celnej większość jej pracowników stanowili Polacy .
W czasie powstania styczniowego czynnie wspierali oni powstańców , co stało się przyczyną represji wymierzonych w nich jeszcze w czasie walk ( czerwiec–lipiec 1863 ) .
Polaków zastępowali napływowi pracownicy rosyjscy , wyznawcy prawosławia .
Na ich potrzeby w 1876 lub 1877 została wybudowana cerkiew .
Cerkiew została zlokalizowana na placu sąsiadującym z dworcem kolejowym i wzniesiona według projektu synodalnego architekta Trusowa .
W 1875 na jej budowę wydzielono 35 tys .
rubli z budżetu Imperium Rosyjskiego .
Projekt przygotowany dla świątyni został następnie powtórzony przy budowie kolejnych cerkwi na ziemiach polskich : w Grajewie , w Mławie , w Granicy i w Słupcy .
W 1879 cerkiew odwiedził car Aleksander II , który spotykał się w Aleksandrowie pogranicznym z cesarzem Wilhelmem I. Wizyta miała ratować spójność sojuszu trzech cesarzy .
Według ks .
J. Dębińskiego w czasie I wojny światowej wieża świątyni przewróciła się na dach obiektu , zawalając jego sklepienie .
W rezultacie budynek miał stać się niezdatny do użytku .
Kiriłł Sokoł i Aleksander Sosna podają , że w latach 20 .
XX wieku została zniszczona kopuła i dzwonnica cerkwi , następnie rozebrano cały obiekt .
Skibińska łączy to wydarzenie z falą rozbiórek cerkwi ( lub ich adaptacji na kościoły ) w ramach usuwania z przestrzeni publicznej obiektów kojarzących się z zaborcą rosyjskim .
Tadeusz Bakalarczyk pisze , iż cerkiew nie była użytkowana od wyjazdu Rosjan w czasie I wojny światowej , zaś decyzja o jej rozebraniu wynikała z fatalnego stanu technicznego porzuconego budynku .
Natomiast Ewa Skibińska podaje , iż cerkiew była jeszcze czynna na początku lat 20 .
XX wieku i była miejscem modlitwy dla prawosławnych Ukraińców osadzonych w Obozie Internowania nr 6 .
Zofia Waszkiewicz pisze , iż internowani korzystali ze świątyni jedynie do Bożego Narodzenia w r. 1921 ( według kalendarza juliańskiego ) .
7 stycznia tego roku , po zakończonej Świętej Liturgii , Ukraińcy przeszli w pochodzie do obozu , co zostało odczytane jako manifestacja polityczna i sprawiło , że zabroniono im udawania się na nabożeństwa do miasta .
Od tej pory miejscem kultu internowanych była jedynie kaplica obozowa .
Część materiału budowlanego pozyskanego po rozbiórce cerkwi wykorzystano przy budowie kamienicy przy ul .
Juliusza Słowackiego 7 w Aleksandrowie .
Wyposażenie cerkwi udało się jednak w dużej części uratować .
Znalazło się ono w kaplicy św .
Aleksandra Newskiego urządzonej w pierwotnym domu proboszcza .
Jest ona filią parafii prawosławnej w Toruniu .
Cerkiew znajdowała się na gruncie należącym do kolei .
Po II wojnie światowej obszar ten został znacjonalizowany .
Na miejscu pierwotnej cerkwi , położonej przy dzisiejszej ulicy Wojska Polskiego , znajduje się dziś market .
W 2006 parafia św .
Mikołaja w Toruniu wystąpiła do władz Aleksandrowa Kujawskiego o zwrot gruntu , na którym znajdowała się cerkiew .
Architektura .
Wyświęcona w 1877 ceglana cerkiew w Aleksandrowie Kujawskim posiadała dwie kryte blachą kopuły .
Większa z nich , wsparta na ośmiobocznym bębnie , znajdowała się nad nawą obiektu .
Druga wieńczyła dwukondygnacyjną dzwonnicę z sześcioma miedzianymi dzwonami .
Cały budynek zajmował powierzchnię 2800 metrów kwadratowych .
