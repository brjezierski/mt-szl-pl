Wzniesienie cerkwi św .
Michała Archanioła w Warszawie przypada na okres wzmożonych inwestycji w budownictwie sakralnym , jaki miał miejsce w ostatnich latach panowania Aleksandra III i na początku rządów Mikołaja II na terenie zachodnich rubieży Imperium Rosyjskiego .
Jednym z elementów akcji budowy nowych świątyń prawosławnych było zaopatrywanie we własne cerkwie poszczególnych rosyjskich jednostek wojskowych .
Cerkiew św .
Michała Archanioła była jednym z obiektów o takim przeznaczeniu .
Budowla sakralna była zlokalizowana przy Alejach Ujazdowskich 12 , w rejonie miasta szczególnie licznie zamieszkanym również przez cywilnych Rosjan .
Według wspomnień A. Kraushara obszar ten był szczególnie atrakcyjny dla zamożnych Rosjan .
Na przełomie XIX/XX wieku stracił niemal całkowicie pierwotny , polski charakter .
Pracami budowlanymi kierował inżynier kapitan Lüders .
Obiekt wzniesiono w latach 1892–1894 .
Kamień węgielny pod budowę nowej cerkwi położono 16 czerwca 1892 na miejscu starszej , tymczasowej świątyni wojskowej .
Uroczystości przewodniczył arcybiskup chełmsko-warszawski Flawian , któremu asystowała grupa kapelanów wojskowych oraz dwóch duchownych eparchialnych .
21 grudnia 1894 nastąpiła jej konsekracja .
Cerkiew była częścią kompleksu koszar Litewskiego Pułku Lejbgwardii , które rozciągały się między aleją Szucha a ulicami Litewską i Nowowiejską .
Budowla uważana była za jedną z najbardziej udanych architektonicznie świątyń prawosławnych wzniesionych przez Rosjan w Warszawie .
Miało to związek z elitarnym charakterem pułku , dla którego była przeznaczona .
Nie została wzniesiona według ujednoliconego planu przewidywanego dla cerkwi wojskowych , ale projektu przygotowanego specjalnie na tę okazję , który uwzględniał sposób komponowania się budynku z otoczeniem ( w szczególności z położonymi w sąsiedztwie parkami ) .
