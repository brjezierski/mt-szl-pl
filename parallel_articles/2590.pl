Porzucona , zaczęła popadać w ruinę .
Przez krótki czas użytkowała ją parafia Kościoła ewangelickiego .
Po rozpoczęciu akcji burzenia cerkwi została uznana za niepotrzebną , wywołującą skojarzenia z rosyjską władzą w Warszawie i zburzona w 1923 .
Ryszard Mączewski uważa , że główną przyczyną jej rozbiórki był zły stan techniczny z powodu porzucenia i braku konserwacji .
Zniszczenie świątyni było jednym z epizodów akcji rewindykacji prawosławnych cerkwi , które zostały nagłośnione za granicą , jako dowód nietolerancji wyznaniowej panującej w Polsce .
Na jej miejscu wzniesiono wkrótce pawilony wystawiennicze „ Łobzowianki ” i muszlę koncertową .
Współcześnie w miejscu , w którym znajdowała się świątynia , przebiega w wykopie Trasa Łazienkowska .
Architektura .
Cerkiew posiadała pięć kopuł z krzyżami oraz dzwonnicę z dodatkową kopułą nad wejściem .
Reprezentowała styl starorosyjski ( późną odmianę stylu bizantyjsko-rosyjskiego , oficjalnego stylu Imperium Rosyjskiego ) .
Wszystkie kopuły były pokryte blachą cynkowaną i pomalowane na zielono w szachownicę .
Piotr Paszkiewicz porównuje rozplanowanie świątyni do cerkwi Wasyla Błogosławionego w Moskwie – podobnie jak w przypadku tej budowli , cała kompozycja budynku była złożona z pozornie niezależnych elementów .
Cztery boczne kopuły sprawiały wrażenie mniejszych świątyń połączonych luźno z głównym korpusem zwieńczonym główną , basztowo-namiotową kopułą na bębnie .
