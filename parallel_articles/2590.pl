Cerkiew św .
Michała Archanioła – cerkiew prawosławna w Warszawie , położona w Alejach Ujazdowskich , w rejonie współczesnego placu Na Rozdrożu .
Była to jedna z cerkwi wojskowych wybudowanych na potrzeby rosyjskich żołnierzy stacjonujących w Warszawie , w szczególności służących w Litewskim Pułku Lejbgwardii .
Wzniesiona w latach 90 .
XIX wieku , została zburzona w 1923 , w czasie akcji rewindykacji cerkwi uznanych za symbole rosyjskiej władzy okresu zaborów .
