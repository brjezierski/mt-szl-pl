Cerkiew św .
Mikołaja Cudotwórcy – nieistniejąca obecnie cerkiew w Sosnowcu , znajdowała się przy obecnej ul .
Parkowej .
Jednym z fundatorów cerkwi był Henryk Dietel .
Po 1918 została zamieniona na katolicki kościół św .
Jakuba .
Została zburzona w 1938 r. Według artykułu opublikowanego w regionalnej gazecie „ Torpeda ” decyzja o jej zniszczeniu zapadła po wizycie gen. Składkowskiego w Sosnowcu .
Premier miał określić wzniesioną w stylu neobizantyjskim cerkiew jako „ pięść imperializmu rosyjskiego grożącą Polsce ” .
Świątynię zniszczono mimo protestów rzymskokatolickiego dziekana będzińskiego , ks .
Franciszka Goli , który porównał działalność władz polskich do prowadzonych przez bolszewików prześladowań Rosyjskiego Kościoła Prawosławnego .
Z wyposażenia cerkwi ocalał ikonostas , znajdujący się obecnie przed ołtarzem bocznym soboru Narodzenia Przenajświętszej Bogurodzicy we Wrocławiu .
