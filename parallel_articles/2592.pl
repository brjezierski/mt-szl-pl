Cerkiew Świętych Apostołów Piotra i Pawła w Augustowie
Cerkiew Świętych Apostołów Piotra i Pawła – niezachowana cerkiew prawosławna w Augustowie .
Do czasu wybudowania cerkwi Świętych Apostołów Piotra i Pawła w Augustowie istniała tylko niewielka drewniana cerkiew Kazańskiej Ikony Matki Bożej .
Decyzję o budowie nowej świątyni podjął w 1878 gubernator suwalski Siergiej Gołowin na wniosek 24 prawosławnych obywateli Augustowa .
Rok później miasto wyznaczyło teren pod budowę na środku ogrodu spacerowego ( obecnie park na Rynku Zygmunta Augusta ) .
Prace planistyczne zakończono w lutym 1881 , a 9 czerwca 1881 arcybiskup chełmsko-warszawski Leoncjusz ( Lebiedinski ) poświęcił kamień węgielny .
Cerkiew otwarto 7 października 1884 .
Budowę cerkwi sfinansowano z państwowego funduszu cerkiewno-budowlanego .
Prace wykonywali majstrowie z Górnego Śląska .
Świątynia mogła pomieścić 800 osób .
Cerkiew miała jedną kopułę oraz czteropiętrową dzwonnicę z pięcioma dzwonami .
Do cerkwi przeniesiono ikonę Kazańskiej Matki Bożej .
Mieszkańcy Augustowa ofiarowali też ikonę św .
Aleksandra Newskiego , którą umieszczono w nowym kiocie .
W 1887 w cerkwi umieszczono też ikonę Cerkiewnej Matki Bożej , zwanej Jordańską .
W 1894 przy świątyni powołano bractwo cerkiewno-parafialne , które prowadziło działalność charytatywno-społeczną .
Ostatnie nabożeństwo w cerkwi Świętych Piotra i Pawła było odsłużone , za pozwoleniem władz , w dni Wielkanocne 1924 r. W 1926 cerkiew została rozebrana .
Obecnie w miejscu cerkwi znajduje się pomnik Zygmunta Augusta .
W 2017 rozpoczęto przy ul .
Mazurskiej w Augustowie budowę nowej cerkwi , która architektoniczne ma nawiązywać do cerkwi Świętych Apostołów Piotra i Pawła .
