Cerkiew Świętych Wiery , Nadziei , Luby i matki ich Zofii w Sosnowcu
Cerkiew pod wezwaniem Świętych Wiery , Nadziei , Luby i matki ich Zofii ( `` Świętych Wiary , Nadziei i Miłości '' ) – jedyna istniejąca z trzech sosnowieckich cerkwi prawosławnych .
Znajduje się przy ulicy Jana Kilińskiego 39 .
Jest to świątynia parafialna .
Należy do dekanatu Kraków diecezji łódzko-poznańskiej Polskiego Autokefalicznego Kościoła Prawosławnego .
Historia .
Myśl o budowie po raz pierwszy pojawiła się w 1887 .
Zorganizowano składkę wśród sosnowieckich i łódzkich przemysłowców – ludzi tak zasłużonych dla rozwoju Sosnowca , jak : bracia Oscar i Franz Schönowie , Heinrich Dietel , Konrad Gamper , Kunitzy i in .
– przeważnie ewangelików bądź wyznania mojżeszowego , którym zależało na dobrych układach z rosyjską władzą .
Działkę otrzymano bezpłatnie od Kolei warszawsko-wiedeńskiej .
15 sierpnia 1888 położono kamień węgielny , a już 28 listopada roku następnego , świątynia została poświęcona przez biskupa lubelskiego Flawiana .
Patronkami nowej cerkwi zostały : Wiera , Nadieżda i Liubow ' , córki św .
Zofii , męczennice z czasów Hadriana .
Budowa przebiegła bardzo szybko , co odbiło się na jakości budynku .
Nabożeństwa odprawiali pierwotnie księża z sąsiednich parafii na Maczkach , w Olkuszu i w Częstochowie .
Ale już w kwietniu 1890 nowa cerkiew otrzymała pierwszego proboszcza – Joanna Wasiliewicza Lewickiego ( z Kalwarii w Podlaskiem ) .
16 maja 1890 arcybiskup warszawsko-chełmski Leoncjusz potwierdził powstanie samodzielnej parafii przy cerkwi pod wezwaniem Świętych Wiery , Nadjeżdy , Ljub ’ wi i ich matki Zofii , do której przynależał powiat będziński , sztab tutejszej straży granicznej i urzędnicy celni .
Obok powstał dom parafialny – siedziba duchownego i osobny domek dla starosty cerkiewnego .
Dziś cerkiew należy do parafii Świętych Wiery , Nadziei , Luby i matki ich Zofii w Sosnowcu w diecezji łódzko-poznańskiej .
Parafia ( jako jedna z 2 placówek prawosławnych w województwie śląskim ) obejmuje południową część województwa , m.in .
aglomerację katowicką , Bielsko-Białą , Rybnik .
Obecnym proboszczem jest ks .
Mikołaj Dziewiatowski .
16 marca 2014 w świątyni – po raz pierwszy w jej historii – miały miejsce święcenia kapłańskie .
Architektura .
Zbudowana z cegieł i kamienia , cerkiew składa się z pięciu części .
W centrum znajduje się ośmiobok , który z jednej strony podtrzymuje wieżę , a z drugiej prezbiterium .
Środkową część świątyni przykrywa kopuła , którą widać jednak tylko od wewnątrz , ponieważ całość uwieńczona jest cebulastym hełmem .
Wejście do cerkwi stanowi fronton w kształcie `` oślego grzbietu '' oparty na dwóch , wyraźnie oddzielonych od muru , `` gruszkowatych '' kolumnach , tworząc wrażenie „ dwuwarstwowości ” .
Wnętrze budowli w odróżnieniu od wyglądu zewnętrznego , nosi na sobie znaki okcyndentalizacji – jest jasne , wykonane w stylu klasycystycznym .
Dębowy ikonostas ( wym .
4,7 x 7 m ) , którego twórcą był artysta moskiewski Liebiediew , a fundatorami bracia Schön , także posiada znaki wpływów zachodnich – szczególnie widoczne jest to w stylu ikon .
Częstochowska Ikona Matki Bożej ( umieszczona w kiocie po lewej stronie ołtarza ) jest darem kolejarzy z Częstochowy .
Od 2017 w cerkwi znajduje się ikona patronek , napisana na Athos ( w której umieszczono relikwie Świętych Wiary , Nadziei , Miłości i matki ich Zofii , sprowadzone w latach 80 .
XX w. z Rzymu do sosnowieckiej świątyni ) .
Cerkiew wpisano do rejestru zabytków 10 października 1980 pod nr A-1242/80 ( obecnie A/831/2021 ) .
