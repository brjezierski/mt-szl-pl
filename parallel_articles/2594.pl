Tscherms
Tscherms ( wł .
`` Cermes '' ) – miejscowość i gmina we Włoszech , w regionie Trydent-Górna Adyga , w prowincji Bolzano .
Liczba mieszkańców gminy wynosiła 1393 ( dane z roku 2009 ) .
Język niemiecki jest językiem ojczystym dla 95,54 % , włoski dla 4,37 % , a ladyński dla 0,09 % mieszkańców ( 2001 ) .
