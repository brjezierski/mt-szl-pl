Cerro Largo ( Urugwaj )
Cerro Largo – departament Urugwaju położony w północno-wschodniej części
kraju .
Cerro Largo graniczy od północy oraz wschodu z brazylijskim stanem
Rio Grande do Sul .
Na południu graniczy z departamentem Treinta y Tres , na południowym zachodzie z Durazno , a na zachodzie oraz północnym zachodzie z departamentami Tacuarembó i Rivera .
Ośrodkiem administracyjnym , a także największym miastem tego powstałego w 1821 departamentu jest Melo .
Powierzchnia Cerro Largo wynosi 13 648 km² .
W 2004 r. departament zamieszkiwało 86 564 osób .
Dawało to gęstość zaludnienia 6,3 mieszkańców/km² .
