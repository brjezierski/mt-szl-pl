Certaldo
Certaldo – miejscowość i gmina we Włoszech , w regionie Toskania , w prowincji Florencja .
Miejsce urodzenia i śmierci , a także pochówku Boccaccia .
Słynie z cebuli o smaku niespotykanym nigdzie , dlatego cebula jest w herbie i na szyldach sklepów .
Atrakcją turystyczną jest wyjazd kolejką linową do górnej części miasta .
Według danych na rok 2004 gminę zamieszkiwały 15 642 osoby , 208,6 os./km² .
