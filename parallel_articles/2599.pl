Cesena ( stacja kolejowa )
Cesena ( włoski : `` Stazione di Cesena '' ) – stacja kolejowa w Cesenie , w regionie Emilia-Romania , we Włoszech .
Znajdują się tu 2 perony .
Jest zarządzana przez Centostazioni i Rete Ferroviaria Italiana .
Stacja została otwarta 1 września 1861 wraz z odcinekiem Bolonia-Forlì , częścią linii Bolonia - Akona .
Rocznie z usług stacji korzysta około 2,5 mln pasażerów .
