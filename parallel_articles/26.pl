.pl
.pl – krajowa domena najwyższego poziomu ( ccTLD ) przypisana do polskich stron internetowych .
Zarządzana i utrzymywana przez Rejestr domeny .pl prowadzony przez NASK .
Początkowo NASK obsługiwał abonentów domeny .pl bezpośrednio .
W latach 2015–2017 wygasił jednak bezpośrednią obsługę .
Postawił na zbudowanie sieci firm partnerskich , które zajmą się obsługą abonentów i będą konkurować pomiędzy sobą jakością obsługi i cenami .
Od tamtej pory abonenci są obsługiwani wyłącznie przez inne podmioty tworzące sieć partnerów NASK .
W 1990 roku IANA zarządzająca wówczas domenami najwyższego poziomu , dokonała wpisu oznaczenia .pl dla domeny krajowej przypisanej administracyjnie do Polski .
Urodziny domeny .pl obchodzimy 30 lipca .
Pierwszą zarejestrowaną polską subdomeną była `` .pwr.pl '' , a należała ona do Politechniki Wrocławskiej .
We wrześniu 2003 została wprowadzona przez NASK technologia IDN , dzięki czemu można w nazwach domen korzystać ze znaków łacińskich , greckich , hebrajskich i cyrylicy .
Na rok 2020 nie wspiera .
