Cestohowa
Cestohowa – miejscowość w USA , w południowej części stanu Teksas , na południowy wschód od San Antonio , w hrabstwie Karnes .
Osada rolnicza , założona w 1873 przez 40 rodzin Górnoślązaków z pobliskiej ( oddalonej o około 5 mil / 8 km na południe ) miejscowości Panna Maria .
Nazwa miejscowości to fonetyczny zapis brzmienia nazwy polskiego miasta Częstochowa w teksańskiej odmianie dialektu śląskiego .
W latach 1877-78 wybudowano kościół .
W latach 1883-1918 Cestohowa miała własną pocztę .
Publiczna szkoła powstała z przekształcenia szkółki parafialnej w roku 1937 i z przerwami działała do późnych lat siedemdziesiątych XX wieku .
