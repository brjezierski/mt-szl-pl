Ceylon
Ceylon ( herbata cejlońska ) – rodzaj herbaty , produkowany na Sri Lance .
Jest to herbata czarna , w pełni sfermentowana .
Najlepsze jej zbiory datowane są na luty , marzec , sierpień i wrzesień , jednakże zbierana jest ona przez cały rok .
Herbata cejlońska charakteryzuje się pełnym , wyrazistym smakiem i czerwono-brązowym kolorem .
Jest to gatunek najbardziej zbliżony do tego , co w Europie i Ameryce zwykło się uważać ogólnie za herbatę .
Zaparza się ją poprzez zalanie gotującą się wodą i odczekanie 2–3 minut .
Proces produkcji herbaty jest podobny niemalże na całej wyspie .
