Chabernica
Chabernica ( `` żytnia baba '' , chaber – roślina z rodziny astrowatych ) – polny demon żeński na Śląsku , w okolicach Prudnika .
Odpowiedniczka południcy .
Wierzenia .
Wyobrażano ją jako piękną , wysoką , smukłą , błękitnie ubraną niewiastę z wieńcem chabrów na głowie .
Pojawiała się tylko w południe i wyłącznie na polu , gdzie pilnowała , by dzieci w poszukiwaniu kwiatów nie deptały zboża i nie wbiegały pomiędzy łany .
Denerwowało ją , gdy widziała , że używają ostrych narzędzi , i gdy żniwiarze nie mieli kapeluszy .
Ci , którzy jej zdaniem zasługiwali na karę , byli usypiani jej słodkim szeptem , a następnie zsyłała na nich ostry ból głowy , paraliż lub postrzał .
Czasem rzucała się na swoją ofiarę i łamała jej ręce i nogi , lub skręcała kark .
Aby uchronić się przed gniewem Chabernicy , należało w południe na Anioł Pański zrobić sobie przerwę od pracy i przesiedzieć ten czas w cieniu .
Znana jest historia o dziewczynce , która zabłądziła na polu i zaplątała się w zaroślach kolczastych jeżyn .
Chabernica , reagując na płacz dziewczynki , uwolniła ją rozgarniając jeżyny i zaprowadziła ją pod jej dom .
Wytłumaczenie .
`` Słownik gwar polskich '' pod redakcją Mieczysława Karasia opisuje chabernicę jako „ strach , którym się straszy dzieci , aby nie wchodziły w zboże ” i notuje jej występowanie w Olbrachcicach w powiecie prudnickim .
Feliks Pluta w „ Kwartalniku Opolskim ” w 1971 cytował : „ Jak my chciały iść na pole ( za dzieci ) , to dycki ojcowie powiadali – nie leć tam , bo cię weźmie chabernica , a jest odziono światło .
No to jak my to szły , to my ino kukały , ale chabernicy my nigdy nie widziały .
To był ino postrach , co by się po zbożu nie dreptało ” .
