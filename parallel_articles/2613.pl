Chaguanas
Chaguanas − miasto i gmina ( Korporacja ) w Trynidadzie i Tobago , leżące na wyspie Trynidad .
Według danych z 2008 roku miasto ma 83 516 mieszkańców .
Jest to największe miasto państwa .
Obecnym burmistrzem miasta jest Orlando Nagessar .
Prócz w Chaguanas obrębie gminy znajduje się miejscowość Cunupia
