Język chakaski
Język chakaski ( inne nazwy : abakański , jenisejo-turecki lub turko-jenisejski ) – język należący do grupy północnej języków tureckich ( wg starszych opracowań – do grupy wschodniej ) , używany przez ponad 40 tys .
użytkowników mieszkających nad rzeką Abakan ( lewy dopływ górnego Jeniseju ) w Chakaskim Okręgu Autonomicznym w Kraju Krasnojarskim na Syberii .
