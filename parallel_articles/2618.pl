Chalan Kanoa
Chalan Kanoa ( 3251 mieszkańców w 2009 ) – miejscowość wypoczynkowa na zachodnim wybrzeżu wyspy Saipan , podawana często za stolicę Wspólnoty Marianów Północnych .
Ósme co do wielkości miasto kraju .
