Morfologia .
Teleomorfa to apotecjum białawe do kremowego , z wiekiem brązowiejące , zbudowane z płaskiego kapelusza o średnicy 1,5–3,0 mm i krótkiego trzonka o wymiarach 0,4–2,0 × 0,2–0,5 mm powiększonego lub wąskiego u podstawy , część podstawy często czarna .
Worki cylindryczno-maczugowate , 8-zarodnikowe , o rozmiarach około 80–107 × 6–12 µm .
Zarodniki cylindryczno-wrzecionowate , szkliste , dekstrynoidalne , w stanie dojrzałym z 1 ( -2 ) przegrodą , o rozmiarach około 13–17 ( –21 ) × 3,5–5,0 µm .
Wstawki z przegrodą , szkliste , lekko żółtawe , cylindryczne o szerokości 2,0–2,5 µm , powiększone do 3 µm na szczycie .
