Hymenoscyphus fraxineus
Hymenoscyphus fraxineus ( T. Kowalski ) Baral , Queloz & amp ; Hosoya – gatunek grzybów z rodziny tocznikowatych ( `` Helotiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenoscyphus , Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 2006 Tadeusz Kowalski nadając mu nazwę `` Chalara fraxinea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w roku 2014 Hans-Otto Baral , Valentin Quelo i Tsuyoshi Hosoya .
