Chalkida ( gr .
Χαλκίδα ; hist .
`` Chalkis '' ) – miasto w Grecji , na wyspie Eubea , nad cieśniną Ewripos Morza Egejskiego , w administracji zdecentralizowanej Tesalia-Grecja Środkowa , w regionie Grecja Środkowa , w jednostce regionalnej
Eubea .
Siedziba gminy Chalkida .
W 2011 roku liczyło 59 125 mieszkańców .
Miasto znane już w starożytności ( prowadziło aktywną akcję kolonizacyjną ) , wzmiankowane w `` Iliadzie '' .
W 1981 roku miało 36 tys .
mieszkańców , a w 2001 ok. 53,6 tys .
mieszkańców .
