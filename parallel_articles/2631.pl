Chandrayaan-1
Chandrayaan-1 ( sanskryt चंद्रयान-1 , dosłownie „ księżycowy statek–1 ” ) – pierwsza indyjska sonda kosmiczna i sztuczny satelita Księżyca .
Sonda składała się z orbitera i niewielkiego lądownika ( impaktora ) , który uderzył w powierzchnię Księżyca .
Całkowity koszt misji szacowany jest na 80-100 mln USD .
Konstrukcja sondy .
Sonda zbudowana została w oparciu o konstrukcję satelity meteorologicznego Kalpansat .
Kadłub ma kształt sześcianu o długości boku 1,5 m. Do boku statku przymocowany jest pojedynczy panel baterii słonecznych o wymiarach 2,15 × 1,8 m dostarczający energii o mocy około 750 W. Ładuje on akumulatory litowo-jonowe o pojemności 36 Ah .
Łączność z Ziemią w paśmie X zapewniała antena główna o średnicy 0,7 m. Sonda zaopatrzona jest w silnik główny o ciągu 440 N i zestaw 8 silników kontroli położenia o ciągu 22 N. Sonda jest stabilizowana trójosiowo .
Na szczycie statku , do momentu jego uwolnienia , znajdował się lądownik ( Moon Impact Probe ) o masie około 35 kg i wymiarach 37,5 cm × 37,5 cm × 47,0 cm , zaopatrzony w niewielki silnik hamujący .
Całkowita masa startowa sondy wynosiła 1380 kg , a po dotarciu na orbitę wokół Księżyca spadła do 675 kg .
