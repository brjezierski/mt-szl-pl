Przebieg misji .
Po wyniesieniu z kosmodromu Sriharikota , 22 października 2008 , sonda została wprowadzona początkowo na orbitę o perygeum wynoszącym 255 km , apogeum 22 860 km i nachyleniu 17,9° .
W następnych dniach kilkakrotnie podniesione zostało apogeum orbity , aż do wejścia na wstępną orbitę eliptyczną wokół Księżyca .
