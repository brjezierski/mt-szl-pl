Changzhou
Changzhou ( chiń .
常州 ; pinyin : `` Chángzhōu '' ) – miasto o statusie prefektury miejskiej we wschodnich Chinach , w prowincji Jiangsu , port nad Wielkim Kanałem , na południowym brzegu Jangcy .
W 2010 roku liczba mieszkańców miasta wynosiła 1 000 831 .
Prefektura miejska w 1999 roku liczyła 3 397 108 mieszkańców .
Ośrodek przemysłu bawełnianego , maszynowego i spożywczego .
W znajdującej się w Changzhou świątyni Tianning można podziwiać najwyższą na świecie buddyjską pagodę .
