Chania
Chania ( , , ) – miasto w południowej Grecji , na północno-zachodnim wybrzeżu Krety , w administracji zdecentralizowanej Kreta , w regionie Kreta , w jednostce regionalnej Chania .
Siedziba gminy Chania .
Zlokalizowane na wybrzeżu Morza Śródziemnego ( Morza Kreteńskiego ) , nad Zatoką Chania .
W 2011 r. liczyło 53 910 mieszkańców ( drugie pod tym względem miasto wyspy ) .
W latach 1898–1971 główny ośrodek administracyjny Krety .
Miasto zostało założone przez Wenecjan na miejscu starożytnego , istniejącego tu już ok. 3400 p.n.e. , minojskiego osiedla Kydonia .
Obecnie Chania słynie z atrakcyjnej nabrzeżnej dzielnicy portowej i charakterystycznej zabudowy z czasów weneckich .
W okolicach miasta znajduje się śródziemnomorska baza sił zbrojnych USA oraz port lotniczy im .
