Charbinowice
Charbinowice – wieś sołecka w Polsce położona w województwie świętokrzyskim , w powiecie kazimierskim , w gminie Opatowiec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
Historia .
Pierwsza wzmianka o miejscowości pochodzi z 1278 r. Książę Leszek Czarny potwierdził wówczas zakup wsi przez miecznika sandomierskiego Klemensa , za sumę 150 grzywien .
Wieś została zwolniona z prawa polskiego , a Klemens otrzymał prawo do odprawiania na miejscu sądów .
W źródle z 1386 r. miejscowość występuje pod nazwą `` Charbinowicze '' .
Do połowy XV w. wieś była własnością rodu Charbinowskich .
Kolejnym właścicielem był Stanisław z Tęczyna herbu Topór .
