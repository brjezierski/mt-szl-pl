Obwód charkowski
Obwód charkowski ( ) – jeden z 24 obwodów Ukrainy .
Leży we wschodniej części Ukrainy , od północy graniczy z Rosją , od wschodu z obwodem ługańskim , od południowego wschodu z obwodem donieckim , od południowego zachodu z obwodem dniepropetrowskim , od zachodu z obwodem połtawskim , od północnego zachodu z obwodem sumskim .
Obwód zajmuje powierzchnię 31 415 km² , co stanowi 5,2 % powierzchni Ukrainy .
Obwód charkowski jest trzecim najbardziej zaludnionym obwodem na Ukrainie .
Ponad połowa mieszkańców mieszka w stolicy obwodu Charkowie .
W stolicy okręgu większość mieszkańców posługuje się językiem rosyjskim , na prowincji natomiast ludność używa dialektu stanowiącego mieszankę języka rosyjskiego i ukraińskiego , zwanego surżyk .
Obwód leży na obszarze Ukrainy Słobodzkiej ( północna i środkowa część ) i Zaporoża ( południowa część ) .
Historia .
Terytorium obwodu zostało zaludnione w późnym paleolicie ( 10–12 tys .
lat temu ) , lecz archeologowie odkryli na tym terenie ślady człowieka neandertalskiego pochodzące sprzed 80 tys .
lat .
W czasach Jagiellonów obszar znajdował się w granicach Wielkiego Księstwa Litewskiego .
W 1503 Litwa utraciła północno-wschodnią część regionu .
Południowo-zachodnia część w 1569 przeszła w granice Korony Królestwa Polskiego .
