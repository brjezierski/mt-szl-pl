Charków
Charków ( , `` Charkiw '' ; , `` Char´kow '' ) – miasto w północno-wschodniej części Ukrainy , położone na południowym krańcu Wyżyny Środkoworosyjskiej , stolica obwodu charkowskiego .
Na początku 2021 roku , z liczbą mieszkańców wynoszącą około 1,43 mln , Charków zajmował drugie miejsce ( po Kijowie ) wśród najludniejszych ukraińskich miast .
Jest jednym z dziesięciu największych miast Ukrainy pod względem powierzchni .
Charków jest jednym z największych ośrodków przemysłowych i kulturalno-naukowych Ukrainy .
W mieście rozwija się przemysł maszynowy , zbrojeniowy , metalowy , chemiczny , środków transportu , materiałów budowlanych , włókienniczy i spożywczy .
Był trzecim ośrodkiem naukowym i przemysłowym ZSRR po Moskwie i Leningradzie .
Odbywają się w nim Międzynarodowe Targi Przemysłu Obronnego .
Działa kilkanaście uniwersytetów , akademie i inne szkoły wyższe .
Miasto leży nad trzema niewielkimi rzekami : Charkiw ( dopływ rzeki Łopań ) , Łopań ( dopływ rzeki Uda ) i Uda ( dopływ Dońca ) .
Aglomeracja w 2019 roku liczyła 2 109 000 mieszkańców .
Językiem dominującym jest rosyjski .
Od marca 2022 Miasto-bohater Ukrainy .
