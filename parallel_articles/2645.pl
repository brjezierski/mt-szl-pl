Charles Aznavour
Charles Aznavour , właśc .
Szahnur Waghinak Aznawurian ( ; ur .
22 maja 1924 w Paryżu , zm .
1 października 2018 w Mouriès ) – francuski piosenkarz , kompozytor , autor tekstów piosenek i aktor filmowy pochodzenia ormiańskiego .
Był niewysokiego wzrostu , obdarzony charakterystycznym głosem , w drugiej połowie XX wieku stał się jedną z najbardziej rozpoznawalnych postaci światowej muzyki rozrywkowej .
Skomponował około 1000 piosenek ( w tym ok. 150 do tekstów po angielsku , 100 po włosku , 70 po hiszpańsku i 50 po niemiecku ) .
Sprzedał ponad 200 milionów płyt na całym świecie .
Wystąpił w ponad 60 filmach .
Bywał nazywany francuskim Frankiem Sinatrą .
Tematem jego piosenek była na ogół miłość .
Artysta mówił i śpiewał w wielu językach : po francusku , angielsku , włosku , neapolitańsku , hiszpańsku , niemiecku , rosyjsku , portugalsku i po ormiańsku .
W tym ostatnim języku nagrał XVIII-wieczną pieśń poety Sajat-Nowy .
Z kolei piosenka `` Que C ’ est Triste Venise '' ( `` Jakże smutna jest Wenecja '' ) została przez niego zaśpiewana w rekordowej liczbie wersji : włoskiej ( `` Com ' è Triste Venezia '' ) , hiszpańskiej ( `` Venecia Sin Ti '' ) , angielskiej ( `` How Sad Venice Can Be '' ) i niemieckiej ( `` Venedig im Grau '' ) .
Życiorys .
Dzieciństwo .
Urodził się w Paryżu jako syn ormiańskich emigrantów : Mikajela Aznawuriana i Knar Baghdasarian .
Jego ojciec urodził się w Achalciche w Gruzji , a matka pochodziła z kupieckiej rodziny z Turcji .
Oboje rodzice zatrzymali się tymczasowo we Francji w drodze do USA .
Musieli czekać na wizę wjazdową , gdy na świat przyszedł ich syn .
Muzykalni rodzice pierwsi odkryli u swego dziecka talent muzyczny ( mały Charles uczył się grać [ krótko ] na skrzypcach i później na pianinie ) i bardzo wcześnie wprowadzili je w świat teatru .
