Charlotte Rampling
Tessa Charlotte Rampling , OBE ( ur .
5 lutego 1946 w Sturmer ) – angielska aktorka i modelka .
Występuje w filmach anglo- i francuskojęzycznych .
Życiorys .
Wczesne lata .
Urodziła się w Sturmer , w hrabstwie Essex jako jedna z dwóch córek malarki Anne Isabelle ( z domu Gurteen ) i Godfreya Ramplinga , złotego medalisty olimpijskiego w sztafecie 4 x 400 metrów na igrzyskach olimpijskich w Berlinie ’ 36 , i pułkownika Armii Brytyjskiej .
Po raz pierwszy wystąpiła publicznie w trzynastym roku życia – wraz ze starszą siostrą Sarah ( która w 1966 popełniła samobójstwo ) śpiewała znane piosenki ( „ C ’ est si bon ” czy „ C ’ est magnifique ” ) w londyńskich pubach .
Ukończyła St. Hilda ’ s School w Bushley w hrabstwie Worcestershire i Jeanne d ’ Arc Academie pour Jeunnes Filles w Wersalu .
