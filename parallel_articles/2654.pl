Charlottesville
Charlottesville ( C-Ville , Hoo-Ville ) jest niezależnym , prowincjonalnym miastem , leżącym wewnątrz hrabstwa Albemarle w stanie Wirginia .
Populacja miasta wynosi ok. 40 tys .
