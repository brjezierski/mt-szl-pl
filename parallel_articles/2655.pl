Charmanli
Charmanli ( bułg .
`` Харманли '' ) – miasto w południowej Bułgarii , w obwodzie Chaskowo , siedziba administracyjna gminy Charmanli .
Według danych Narodowego Instytutu Statystycznego w Bułgarii z 31 grudnia 2011 miasto liczyło 18429 mieszkańców .
W mieście rozwinął się przemysł włókienniczy , spożywczy , drzewny oraz ceramiczny .
Położenie .
Nad ujściem rzeki Charmanlijskiej do Maricy .
Miejscowość ta znajduje się na drodze .
