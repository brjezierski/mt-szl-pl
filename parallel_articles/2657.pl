Chartum
Chartum ( arab .
الخرطوم , czyli „ trąba słonia ” ) – stolica Sudanu , położona w miejscu , w którym łączą się wody Nilu Białego i Błękitnego .
Główny ośrodek polityczny , handlowy i kulturalny kraju , ważny węzeł komunikacyjny .
Uniwersytet założony w 1903 .
Ludność : 2203,9 tys .
mieszkańców ( 2008 ) - drugie pod względem wielkości miasto kraju .
Wraz z sąsiednimi miastami Omdurmanem i Chartumem Północnym tworzy zespół miejski , który zamieszkuje około 4,8 mln mieszkańców .
Geografia .
Chartum leży w dolinie Nilu , w miejscu , gdzie Nil Biały na drodze do Morza Śródziemnego przyjmuje jeden z ostatnich dopływów niebędący rzeką okresową , Nil Błękitny .
Miasto leży w strefie zwrotnikowej na wysokości 385 m n.p.m. , na południowo-wschodnim krańcu Sahary .
Panuje tu klimat kontynentalny suchy , który jest tylko nieznacznie łagodzony przez obecność położonej na południowym wschodzie Wyżyny Abisyńskiej .
Roczne opady wynoszą 196 mm , a ich maksimum przypada na okres od czerwca do września .
Niewielka ilość opadów sprawia , że o rozwoju miasta decyduje obfitość wód Nilu i wody gruntowe w dolinie rzeki .
Średnie miesięczne temperatury to 22,4 °C w styczniu oraz 30,7 °C w lipcu .
