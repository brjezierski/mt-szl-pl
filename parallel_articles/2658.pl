Chaskowo
Chaskowo ( ) – miasto w południowo-wschodniej Bułgarii , ośrodek administracyjny obwodu Chaskowo , u podnóża północnych Rodopów , nad rzeką Chaskowską ( dopływ Maricy ) .
Według danych Narodowego Instytutu Statystycznego w Bułgarii , 31 grudnia 2011 roku miasto liczyło 75641 mieszkańców .
Historia .
Badania archeologiczne wykazały , że pierwsza osada na terenie dzisiejszego miasta powstała około 5000 lat p.n.e .
Chaskowo zostało założone w XIII wieku .
Przemysł .
Chaskowo to duży ośrodek przemysłu tytoniowego z licznymi wytwórniami papierosów .
Działa tu również przemysł włókienniczy , odzieżowy , maszynowy .
