Chaville
Chaville – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Według danych na rok 2012 gminę zamieszkiwały 19 343 osoby , a gęstość zaludnienia wynosiła 5418 osób/km² .
Wśród 1287 gmin regionu Île-de-France Chaville plasuje się na 782. miejscu pod względem powierzchni .
