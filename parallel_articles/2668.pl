Chelsea F.C .
Chelsea Football Club ( wym .
// ) – angielski , profesjonalny klub piłkarski mający swoją siedzibę w zachodnim Londynie , w dzielnicy Hammersmith and Fulham .
Został założony 10 marca 1905 roku , obecnie gra w Premier League i większość sezonów spędził w najwyższej klasie rozgrywkowej w Anglii .
Klub Chelsea F.C .
jest sześciokrotnym mistrzem Anglii , osiem razy zdobył Puchar Anglii , pięć razy Puchar Ligi Angielskiej i dwukrotnie zwyciężył w rozgrywkach Ligi Europy UEFA oraz Pucharu Zdobywców Pucharów .
Chelsea jest również dwukrotnym triumfatorem Ligi Mistrzów UEFA z sezonu 2011/2012 i 2020/2021 .
Pierwszym tryumfem klubu było zdobycie tytułu mistrza Anglii w 1955 roku .
Chelsea wygrała kilka rozgrywek w latach 60. i 70. , jednak później nie osiągnęła żadnego większego sukcesu do 1997 roku .
Ostatnia dekada była najbardziej udaną w historii klubu .
Chelsea zdobyła kolejne tytuły Mistrza Anglii w 2005 , 2006 oraz w 2010 roku , zaś w 2008 po raz pierwszy dotarła do finału Ligi Mistrzów .
Chelsea swoje domowe mecze rozgrywa na stadionie Stamford Bridge mogącym pomieścić 41 837 kibiców , znajdującym się w Zachodnim Londynie .
Występują na nim od czasu założenia klubu w 1905 roku .
W 2003 roku klub został kupiony przez rosyjskiego magnata naftowego Romana Abramowicza .
Tradycyjne kolory strojów Chelsea to niebieskie koszulki i spodenki oraz białe getry .
