Cheltenham
Cheltenham – miasto w Wielkiej Brytanii ( Anglia ) , w hrabstwie Gloucestershire , w dystrykcie Cheltenham , nad rzeką Chelt ( dopływ Severn ) .
W 2001 roku miasto liczyło 98 875 mieszkańców .
Położone jest w Cotswold , krainie historycznej , zwanej Sercem Anglii .
Mottem miasta jest `` Salubritas et Eruditio ( „ Zdrowie i Wykształcenie ” ) '' .
Dumą Cheltenham są jedyne w Wielkiej Brytanii naturalne wody alkaliczne .
Wody te mają właściwości moczopędne i przeczyszczające , o czym przekonać się można w Town Hall lub Pittville Pump Room , gdzie mieści się również muzeum i galeria mody .
W tutejszym uzdrowisku gościli niegdyś Alfred Tennyson i Thomas Hardy .
W Cheltenham odbywają się corocznie liczne festivale : muzyki ludowej , jazzu , współczesnej muzyki poważnej , literatury , nauki , konkurs Britain In Bloom ( Brytania w kwiatach ) .
W mieście rozwinął się przemysł lotniczy , drzewny , spożywczy oraz farmaceutyczny .
Z Cheltenham pochodził Edward Adrian Wilson , polarnik , towarzysz wypraw Roberta Falcona Scotta , aż po ostatnią .
Cheltenham jest wspomniana w Domesday Book ( 1086 ) jako `` Chinteneham '' .
W Cheltenham urodził się i mieszkał do 1893r .
kompozytor Gustav Holst .
W Cheltenham urodził się i dorastał Brian Jones – założyciel i pierwszy lider The Rolling Stones , pomysłodawca nazwy zespołu , autor koncepcji muzycznej i dużej części dorobku grupy .
Tu też został pochowany .
Z miasta pochodziła brytyjska grupa muzyczna , Stampin ' Ground .
W Cheltenham urodził się Eddie Edwards , skoczek narciarski .
Miasto znane jest również z wyścigów konnych zwanych Cheltenham Gold Cup , a będących jedną z najważniejszych imprez tego typu w Wielkiej Brytanii .
W mieście ma swą siedzibę klub piłkarski Cheltenham Town F.C .
