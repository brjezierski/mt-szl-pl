Chemillé-en-Anjou
Chemillé-en-Anjou – gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku populacja ludności wynosiła 21 237 mieszkańców .
Gmina została utworzona 15 grudnia 2015 roku z połączenia 12 ówczesnych gmin : Chanzeaux , La Chapelle-Rousselin , Chemillé-Melay , Cossé-d ’ Anjou , La Jumellière , Neuvy-en-Mauges , Sainte-Christine , Saint-Georges-des-Gardes , Saint-Lézin , La Salle-de-Vihiers , La Tourlandry oraz Valanjou .
Siedzibą gminy została miejscowość Chemillé .
