Chemnitz
Chemnitz ( w latach 1953–1990 `` Karl-Marx-Stadt '' ) – miasto na prawach powiatu w południowo-wschodnich Niemczech , w Saksonii , nad rzeką Chemnitz , u podnóża Rudaw .
Po Lipsku i Dreźnie trzecie co do wielkości miasto Saksonii .
Nazwa .
Pierwsza wzmianka o miejscowości w formie `` locus Kameniz '' pochodzi z 1143 roku .
Nazwa miasta została ponowiona od nazwy rzeki Chemnitz , która pochodzi z kolei od starołużyckiej nazwy * '' Kamenica '' , utworzonej przez dodanie przyrostka - '' ica '' do słowa * '' kameń '' ‘ kamień ’ .
W latach 1953–1990 miasto nosiło nazwę Karl-Marx-Stadt na cześć Karla Marxa – niemieckiego filozofa i działacza politycznego , jednego z najważniejszych teoretyków i ideologów socjalizmu .
Dawniej stosowaną polską nazwą była Kamienica Saska .
