Chenoa ( piosenkarka )
Chenoa , właściwie María Laura Corradini Falomir ( ur .
25 czerwca 1975 roku w Mar del Plata w Argentynie ) – piosenkarka hiszpańska .
Życiorys .
W wieku ośmiu lat wraz z rodzicami wyemigrowała z Argentyny do Palma de Mallorca w Hiszpanii .
Od szesnastego roku życia śpiewała zawodowo w klubach , pubach i lokalach rozrywkowych na Majorce .
Sukces i sławę zapewnił jej udział w finale telewizyjnego reality show `` Operacion Triunfo '' w 2001 roku , czego efektem była solowa płyta wydana w 2002 roku i sprzedana w ponad półmilionowym nakładzie .
Do tej pory sprzedała ponad milion wszystkich swoich płyt ( osiem albumów ) .
Ponadto jest jurorką hiszpańskiej edycji programu Your Face Sounds Familiar .
