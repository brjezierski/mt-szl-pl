Cheops
Cheops ( z gr . )
– władca starożytnego Egiptu IV dynastii z okresu Starego Państwa .
Imię `` Cheops '' to spopularyzowana przez Herodota , grecka forma egipskiego imienia Chu-fu , skrótu od `` Chnum-chuefui '' ( z egip .
`` Chnum mnie chroni '' ) .
Lata panowania .
Maneton podaje 63 lata rządów , z kolei Herodot przyznaje mu 56 lat , przyjmuje się jednak najczęściej , że panował 23 lata , jak podawał Kanon Turyński .
Biorąc jednak pod uwagę zakres prac budowlanych , niektórzy egiptolodzy przypuszczają , że mógł rządzić 25–35 lat .
Prawdopodobna jest również teza , że Kanon Turyński zawierał niższy stopień datowania , w rzeczywistości chodziło bowiem nie o lata panowania , tylko o spisy ludności , które odbywały się co 2 lata , co dawałoby w sumie 46 lat panowania ( `` Stadelmann '' ) .
