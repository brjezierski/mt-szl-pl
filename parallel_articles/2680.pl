Obwód chersoński
Obwód chersoński ( ) – jeden z 24 obwodów Ukrainy ze stolicą w Chersoniu .
Leży w południowej części Ukrainy , nad Dnieprem , na Nizinie Czarnomorskiej .
Od 2 marca 2022 całkowicie lub częściowo okupowany przez Federację Rosyjską , która ogłosiła aneksję obwodu , nieuznawaną międzynarodowo .
Obwód na północnym zachodzie graniczy z obwodem mikołajowskim , na północy z dniepropietrowskim , na wschodzie z zaporoskim , a na południu z Autonomiczną Republiką Krymu .
Geografia .
Obwód chersoński położony jest na Nizinie Czarnomorskiej nad dolnym odcinkiem Dniepru przy jego ujściu do Morza Czarnego , u nasady półwyspu Krym .
Graniczy z obwodami : mikołajowskim , dniepropietrowskim , zaporoskim i Republiką Autonomiczną Krymu oraz z Morzem Czarnym i Morzem Azowskim .
Na terenie obwodu znajduje się Zbiornik Kachowski na Dnieprze , stepowy rezerwat Askania Nowa i częściowo zalesiony , rozległy półpustynny obszar zwany Dolnodnieprowskie Piaski .
Ludność .
Według ukraińskiego spisu powszechnego 1 stycznia 2021 roku obwód liczył 1 016 707 mieszkańców .
W tym 624 661 w to ludność miejska , 392 046 – mieszkańcy wsi .
