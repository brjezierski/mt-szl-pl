Historia .
Obwód utworzono 30 marca 1944 z połączenia części obwodów mikołajowskiego i zaporoskiego .
W latach 1802–1921 istniała gubernia chersońska z centrum w Chersoniu .
W początkowej fazie inwazji Rosji na Ukrainę , począwszy od 24 lutego 2022 cały obwód wraz z Chersoniem został zajęty przez wojska rosyjskie i znalazł się pod okupacją .
30 września prezydent Rosji Władimir Putin , po pseudoreferendum , ogłosił włączenie do Rosji obwodu chersońskiego , nieuznane przez społeczność międzynarodową .
Po wyparciu Rosjan z Chersonia , od 12 listopada 2022 rosyjską siedzibą władz obwodu został ogłoszony Geniczesk .
