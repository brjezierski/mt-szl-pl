Chełmża ( stacja kolejowa )
Chełmża – stacja kolejowa w Chełmży , w powiecie toruńskim , w województwie kujawsko-pomorskim .
Stacja obsługuje połączenia pasażerskie obsługiwane przez przewoźnika Arriva do Bydgoszczy , Brodnicy , Grudziądza , Torunia i Sierpca .
W roku 2017 stacja obsługiwała 500–699 pasażerów na dobę .
Historia .
Projekt neogotyckiego budynku dworca powstał w 1897 roku ; dwa lata później przystąpiono do jego realizacji wraz z kompleksem budynków towarzyszących .
W 1899 roku przy przejeździe wzniesiono budynek wartowni kolejowej , a w kolejnych latach dokonano rozbudowy torowiska i lokomotywowni ( 1902 ) oraz budowy budynków mieszkalnych dla kolejarzy przy Ładowni ( 1903 ) i ul .
Polnej ( 1907 ) .
Wiosną 2009 roku zamknięto kasę biletową na dworcu .
Po modernizacji stacji uruchomiono automat biletowy .
