Chicago ( film )
Chicago – amerykański musical z 2002 roku w reżyserii Roba Marshalla .
Nominowany do 13 Oscarów .
Film zdobył 6 statuetek .
Film jest przeniesieniem na duży ekran broadwayowskiego musicalu z 1975 roku w reżyserii Boba Fosse , opartego na sztuce pod tym samym tytułem z roku 1926 .
Historia jest luźno oparta na prawdziwej historii z roku 1924 .
Fabuła .
