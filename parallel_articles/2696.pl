Velma Kelly ( Catherine Zeta-Jones ) , gwiazda kabaretu trafia do więzienia oskarżona o zamordowanie męża i siostry , których przyłapała `` in flagranti '' .
Do więzienia trafia też marząca o karierze scenicznej lecz nikomu nieznana Roxie Hart ( Renée Zellweger ) , która w przypływie złości zastrzeliła odchodzącego kochanka .
Przy pomocy prawnika Billy ’ ego Flynna ( Richard Gere ) , Roxie staje się więzienną gwiazdą i ulubienicą prasy .
Velma zepchnięta z piedestału postanawia się zemścić .
