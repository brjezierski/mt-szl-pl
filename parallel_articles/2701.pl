Chiguayante
Chiguayante – miasto i gmina w Chile , w regionie Biobío , w prowincji Concepción .
Należy do obszaru metropolitalnego Gran Concepción .
Powierzchnię gminy liczącą 71,5 km² w 2017 roku zamieszkiwało 85 938 osób , z czego 85 863 osoby przypadały na samo miasto .
Gminę Chiguayante utworzono 28 czerwca 1996 roku .
