Children of Bodom
Children of Bodom – fińska grupa muzyczna wykonująca szeroko pojętą muzykę heavymetalową .
Powstała w 1993 roku w Espoo pod nazwą Inearthed , z inicjatywy gitarzysty i wokalisty Alexi ’ ego Laiho i perkusisty Jaski Raatikainena .
Po nagraniu trzech kaset demo , w 1997 roku zespół przyjął nazwę Children of Bodom .
Nazwa zespołu została zainspirowana niewyjaśnionym morderstwem nad jeziorem Bodom w Finlandii , znajdującym się nieopodal miasta Espoo .
W 2009 roku zespół otrzymał nagrodę fińskiego przemysłu fonograficznego Emma-gaala za album `` Blooddrunk '' ( 2008 ) .
