Język chinalugijski
Język chinalugijski ( nazwa własna Каьатш миI ) – jeden z niewielkich języków kaukaskich , używany przez Chinalugów w północnym Azerbejdżanie .
Stanowisko języka chinalugijskiego w obrębie języków kaukaskich jest sporne .
Według niektórych opracowań język ten należy do zespołu samurskiego , podgrupy dagestańskiej w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Niektórzy jednak językoznawcy uważają , iż język ten jest na tyle różny od pozostałych języków zespołu samurskiego , iż można go z niej wydzielić .
Przy tej klasyfikacji język ten opisywany jest jako izolowany w grupie języków dagestańskich .
Według rozmaitych źródeł język ten używany jest przez 1–3 tys .
osób w kilku osiedlach w północnym Azerbejdżanie .
Nazwa języka wywodzi się od nazwy wsi Chinalug , w którym zamieszkuje największa grupa użytkowników języka .
Język ten nie wykształcił piśmiennictwa .
Jest używany wyłącznie w sytuacjach nieformalnych , w domu , wśród przyjaciół .
W charakterze języka literackiego używany jest natomiast język azerski .
Ze względu na niewielką liczbę użytkowników języka , a także z powodu obniżania się prestiżu języka wśród jego użytkowników , uważa się , iż chinalugijski jest językiem zagrożonym wymarciem w ciągu najbliższych kilkudziesięciu lat .
