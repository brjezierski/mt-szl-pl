Chioggia ( stacja kolejowa )
Chioggia ( wł .
`` Stazione di Chioggia '' ) – stacja kolejowa w Chioggia , w prowincji Wenecja , w regionie Wenecja Euganejska , we Włoszech .
Znajduje się na linii kolejowej Rovigo-Chioggia .
Stacja jest zarządzana przez Rete Ferroviaria Italiana i ma kategorię `` brązową '' .
Historia .
Stacja została otwarta 23 maja 1887 wraz z inauguracją linii Loreo - Chioggia .
Stacja podzieliła losy linii kolejowej : po roku 1905 miała miejsce nacjonalizacja kolei Società italiana per le strade ferrate meridionali do Ferrovie dello Stato ( FS ) .
Od 2000 jest częścią holdingu FS i jest zarządzana przez RFI SpA .
Niedawno otwarto nowe połączenie towarowe z dworca do nowego portu handlowego Chioggia , który jest położony około 500 metrów od stacji .
