Chippenham
Chippenham - miasto w Wielkiej Brytanii , w Anglii w hrabstwie Wiltshire , położone ok. 20 km od Bath nad rzeką Avon , 6 km od autostrady M 4 .
Motto miasta : `` Unity and loyalty '' ( Jedność i lojalność ) .
Nazwa .
Założone w roku 600 miasto nosiło saksońską nazwę `` Cippa '' , oznaczającą osadę nadrzeczną .
Według innych źródeł , miasto pochodzi od słowa `` caep '' oznaczającego targ .
