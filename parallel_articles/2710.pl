Chipping Sodbury
Chipping Sodbury – miasto w Wielkiej Brytanii , w Anglii w South Gloucestershire , położone nad rzeką Frome .
Miasto rozwinęło się wraz z rozwojem sąsiedniego Yate , tworzy z nim konurbację o wielkości 26 855 mieszkańców ( 2001 r. ) .
Miasto posiada najprawdopodobniej najszerszą ulicę w Anglii i jedną z najszerszych głównych ulic w Europie .
Nazwa .
Dość szokująca dla mieszkańców ( wyraz `` sod '' jest nieprzyzwoity ) wywodzi się ze staroangielskiego `` Soppanbyrig '' oznaczającego `` fort Sloppy '' .
`` Chipping '' oznaczało targ .
