Chitonomyces bidessarius
Chitonomyces bidessarius ( Thaxt . )
Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chitonomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1892 r. Roland Thaxter nadając mu nazwę `` Heimatomyces bidessarius '' .
W 1902 r. ten sam autor przeniósł go do rodzaju `` Chitonomyces '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w roku 1994 i 2003 opisał jego występowanie na chrząszcza z rodziny pływakowatych ( Ditiscidae ) : `` Bidessus pusillus '' , `` Bidessus unistriatus '' , `` Hygrotus inaequalis '' .
