Chitonomyces hydropori
Chitonomyces hydropori ( Thaxt . )
Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chitonomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1901 r. Roland Thaxter na owadzie `` Hydroporus modestus '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w roku 1994 i 2003 opisał jego występowanie na chrząszczach z rodziny pływakowatych ( Dytiscidae ) : `` Coelambus impressopunctatus '' , `` Hydroporus erythrocephalus '' , `` Hydroporus umbrosus '' .
