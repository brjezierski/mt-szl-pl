Chitonomyces paradoxus
Chitonomyces paradoxus ( Peyr . )
Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chitonomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1873 r. Johann Joseph Peyritsch nadając mu nazwę `` Heimatomyces paradoxus '' .
W 1902 r. Roland Thaxter przeniósł go do rodzaju `` Chitonomyces '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w roku 1994 opisał jego występowanie na chrząszczach `` Laccophilus hyalinus '' , `` Laccophilus minutus '' i `` Laccophilus variegatus '' z rodziny pływakowatych ( Dytiscidae ) .
