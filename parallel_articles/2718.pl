Chittenango
Chittenango ( w języku Indian oznacza `` Tam gdzie wody się dzielą '' ) – miejscowość w metropolii Syracuse , w hrabstwie Madison , w stanie Nowy Jork , w Stanach Zjednoczonych , założona w 1842 .
Liczba mieszkańców wynosiła 5081 ( 2010 ) .
Liczba mieszkańców całego hrabstwa wynosiła 73 442 ( 2010 ) .
Hrabstwo Madison jest na wysokim czwartym miejscu w rankingu na najlepsze miejsce do życia w płn.-wsch .
części USA .
W Chittenango urodził się Lyman Frank Baum autor serii książek o zaczarowanej `` Krainie Oz '' .
Doskonałe tereny myśliwskie oraz wędkarskie .
W okolicy parki , między innymi : `` Zielone Jeziora '' , `` Kanał Stary Erie `` i `` Wodospad Chittenango '' .
