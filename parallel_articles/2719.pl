Chitungwiza
Chitungwiza , popularnie nazywane „ Chi Town ” – miasto w Zimbabwe , w prowincji Harare , dynamicznie rozwijające się miasto-sypialnia , 30 kilometrów na południe od stolicy .
Liczy obecnie 369,4 tysięcy mieszkańców .
Powstało w 1978 z 3 miejscowości : Seke , Zengeza i St Marys .
Pełne prawa miejskie otrzymało w 1981 .
Jest trzecim pod względem wielkości miastem w Zimbabwe .
W mieście rozwinął się przemysł cementowy oraz spożywczy .
