Pismo chińskie jest systemem otwartym , co oznacza , że ciągle powstają nowe znaki .
W XX wieku wymyślono np .
odrębne znaki dla pierwiastków chemicznych .
System jest pozbawiony jednego , uznawanego powszechnie uporządkowania , którym cechują się alfabety ( np .
od Α do Ω ) , dlatego spotykane czasem określenie „ alfabet chiński ” jest nieścisłe , chyba że chodzi o bopomofo .
Chińskie pismo jest również systemem złożonym , w którym znak z reprezentowanym słowem może łączyć zasada semantyczna , fonetyczna , albo obie naraz .
Dlatego nazywanie go `` pismem ideograficznym '' lub `` pismem obrazkowym '' jest również nieporozumieniem .
