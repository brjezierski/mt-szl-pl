Chińska Republika Ludowa
Chińska Republika Ludowa , ChRL ( , ) , potocznie Chiny ( , ) – państwo w Azji Wschodniej , obejmujące historyczne Chiny ( bez Tajwanu ) oraz Tybet i inne ziemie w Azji Środkowej zamieszkane w sumie przez 56 grup etnicznych .
Chiny to najludniejsze państwo świata o populacji przekraczającej 1,4 mld osób , co stanowi 18,4 % populacji świata .
Pod względem powierzchni jest 3. na świecie , a pod względem wielkości gospodarki 2. po USA ( pod względem PKB nominalnego ) i 1. pod względem PKB realnego .
ChRL graniczy z czternastoma państwami .
Są to : Afganistan , Bhutan , Indie , Kazachstan , Kirgistan , Korea Północna , Laos , Mjanma , Mongolia , Nepal , Pakistan , Rosja , Tadżykistan , Wietnam .
Poza tym zgodnie z polityką migracyjną Hongkongu i Makau ( będących tzw .
Specjalnymi Regionami Administracyjnymi ChRL ) na ich granicach wymagana jest odprawa paszportowo-celna .
Według swojej obecnej konstytucji , Chińska Republika Ludowa jest republiką socjalistyczną .
Geografia .
Ukształtowanie powierzchni .
Większą część Chin stanowią wyżyny i góry .
Niziny zajmują tylko 10 % powierzchni kraju , przy czym duża ich część przypada na stepy i pustynie na północy kraju .
W północno-wschodniej części kraju leży rozległa Nizina Mandżurska ( pow .
350 tys .
km² ) otoczona górami .
Na zachód od niziny ciągnie się Wielki Chingan ( wysokość do 2029 m n.p.m .
) , na północ Mały Chingan i na wschód Góry Wschodniomandżurskie .
Wschodnią część Chin zajmują aluwialne równiny .
Nizina Chińska o powierzchni 300 tys .
km² leży w dolnym biegu Huang He i dorzeczu Huai He .
Znajdują się tam również niziny środkowego i dolnego biegu Jangcy z dużą liczbą jezior .
W południowo-wschodniej części Chin leżą silnie zerodowane Góry Południowochińskie z wysokościami do 2120 m n.p.m .
Na północy kraju wznosi się Wyżyna Mongolska z pustynią Ałaszan oraz Wyżyna Ordos , do której na południu przylega Wyżyna Lessowa – największy w świecie obszar występowania lessów .
Równoleżnikowe pasma górskie ( głównie góry Qin Ling ) oddzielają Wyżynę Lessową od Kotliny Syczuańskiej przechodzącej na południu w Wyżynę Junnan-Kuejczou o charakterystycznej rzeźbie krasowej .
Południowo-zachodnią część kraju zajmuje najwyżej położona wyżyna na Ziemi – Wyżyna Tybetańska ( o wysokościach 4000–5000 m n.p.m . ) .
Wyżynę tę otaczają najwyższe pasma górskie świata – Karakorum ( ze szczytem K2 sięgającym 8611 m n.p.m . )
oraz Pamir ( ze szczytem Kongur Shan o wysokości 7649 m n.p.m . )
od zachodu i Himalaje ( z najwyższym szczytem świata , Mount Everestem , sięgającym 8850 m n.p.m . )
od południa .
Północne obrzeże wyżyny stanowi system górski Kunlun ( najwyższy szczyt to Muztag Feng 6973 m n.p.m . )
obejmujący liczne pasma górskie ( m.in .
Ałtyn-Tag ) rozdzielone tektonicznymi kotlinami m.in .
Kotliną Cajdamską .
Na wschodnim krańcu wyżyny ciągną się góry Hengduan Shan ( ze szczytem Minya Konka o wysokości 7556 m n.p.m . )
rozczłonkowane głębokimi dolinami rzek Saluin , Mekong i Jangcy .
W północno-zachodniej części kraju rozciąga się Sinciang , rozdzielony górami Tienszan na Kotlinę Dżungarską i Kotlinę Kaszgarską .
W obrębie Kotliny Kaszgarskiej znajduje się pustynia Takla Makan i największa depresja Chin – Kotlina Turfańska , zniżająca się do 154 m p.p.m .
Klimat .
Chiny leżą w zasięgu 3 stref klimatycznych : zwrotnikowej ( południowe wybrzeża oraz wyspa Hajnan ) , podzwrotnikowej ( zachodnia , środkowa i wschodnia część kraju ) oraz umiarkowanej ( północne i północno-wschodnie Chiny ) .
Południowo-wschodnia i wschodnia część kraju ma klimat monsunowy z porą deszczową i suchą .
W tej części Chin notuje się najwyższe opady wynoszące 700–1000 mm na wybrzeżu Morza Żółtego , 1500–2000 mm na krańcach południowo-wschodnich i 3000 mm na wyspie Hajnan .
Średnia temperatura w styczniu jest tu zróżnicowana : –5 °C w Pekinie , +12 do +16 °C na wybrzeżu Morza Południowochińskiego , do +20 °C i powyżej na wyspach południowych .
W lipcu temperatura na całym obszarze Chin wschodnich wynosi +25 do +30 °C .
W północno-wschodniej części Chin średnia temperatura w styczniu wynosi –30 °C na północy do –10 °C na południu .
W lipcu te temperatury wynoszą odpowiednio od +20 do +25 °C , a roczna suma opadów 250–1000 mm .
Wewnętrzne i zachodnie obszary kraju mają klimat suchy i kontynentalny .
Średnia temperatura w styczniu jest tu zróżnicowana i wynosi od –8 do –26 °C , a w lipcu waha się od +20 do +25 °C ( w Kotlinie Kaszgarskiej powyżej +30 °C ) .
Opady poniżej 250 mm .
Wyżyna Tybetańska ma klimat chłodny i suchy ze względu na duże wzniesienie nad poziomem morza ze średnią temperaturą w lipcu poniżej +10 °C .
Pora deszczowa na całym obszarze Chin występuje w lecie , z największymi opadami w lipcu i sierpniu .
W lecie i jesienią do wybrzeży południowo-wschodnich docierają tajfuny z ulewnymi deszczami .
Na suchych obszarach zachodu często występują burze pyłowe .
Wody .
Większą część Chin odwadniają rzeki należące do zlewiska Oceanu Spokojnego ( 56,7 % ) .
Do zlewiska Oceanu Indyjskiego należy 6,9 % powierzchni , a do zlewiska Oceanu Arktycznego należy 0,4 % .
Reszta ( czyli 36 % powierzchni ) należy do obszarów bezodpływowych .
Najgęstsza sieć rzeczna występuje na Nizinie Chińskiej , a najrzadsza na pustynnych terenach zachodniej części kraju .
Główne rzeki to Jangcy , Huang He , Xi Jiang , Huai He i Amur ( na granicy z Rosją ) .
Na terenie Chin początek biorą i płyną w znacznych odcinkach wielkie rzeki południowej i wschodniej Azji : Indus , Brahmaputra , Mekong i Saluin .
Chińskie odcinki tych rzek skupiają 1/3 potencjału hydroenergetycznego Chin .
Są one jednak w znikomym stopniu wykorzystywane .
Najdłuższą rzeką na obszarach bezodpływowych jest Tarym .
Chińskie rzeki są intensywnie wykorzystywane do nawadniania zwłaszcza w środkowych i wschodnich Chinach .
We wschodnich Chinach występują katastrofalne powodzie spowodowane dużymi wahaniami stanu wód w rzekach oraz zamuleniem koryt rzecznych .
W przeszłości rzeki Niziny Chińskiej często zmieniały swoje koryta .
Zasoby energetyczne chińskich rzek szacuje się na około 680 GW , z czego 40 % przypada na dorzecze Jangcy , a 20 % Brahmaputry .
We wschodnich Chinach biegnie najdłuższy kanał żeglowny na świecie – Wielki Kanał o długości 1782 km .
Łączy on Pekin z portem w Hangzhou .
Na obszarze Chin występuje około 700 jezior o powierzchni powyżej 100 km² .
Najliczniej występują one na nizinie środkowej i w dolnym biegu Jangcy .
Największe to Poyang Hu o powierzchni 2700–5100 km² , Dongting Hu ( około 4000 km² ) , Hongze Hu i Tai Hu .
Są one wykorzystywane jako naturalne zbiorniki retencyjne .
Na Wyżynie Tybetańskiej znajdują się jeziora głównie słone ( m.in .
Kuku-nor i Nam Co ) .
W suchych regionach regionów autonomicznych Sinciang i Mongolia Wewnętrzna słone jeziora głównie o charakterze reliktowym ( m.in .
Lob-nor , Bosten Hu i Hulun Nur ) .
W północno-wschodnich Chinach , przy granicy z Rosją , duże jezioro Chanka ( po chińsku Xingkai Hu ) .
Największe obszary bagienne w Chinach występują na płaskich , źle odwadnianych terenach północno-wschodnich ( głównie w widłach rzek Ussuri i Sungari ) oraz w południowej części Kotliny Cajdamskiej .
Świat roślinny .
Flora Chin jest bardzo bogata i różnorodna .
Naturalne zbiorowiska leśne zostały w większości zajęte przez pola uprawne , przez co stanowią one dziś tylko 12 % powierzchni kraju .
Na północnym wschodzie w Wielkim Chinganie występuje tajga sosnowo-modrzewiowa przechodząca na południe w dębowo-sosnowe lasy mieszane .
Na Nizinie Chińskiej naturalna roślinność , jaką były lasy monsunowe , została całkowicie wyniszczona .
Na wododziałach zachowały się naturalne lasy liściaste z dębami , wiązami , klonami i jesionami .
Na południe od Jangcy znajdują się lasy z drzewami zimozielonymi ( m.in .
sosny , kuinghamie i cyprysy ) .
W Górach Południowochińskich wiecznie zielone lasy z drzewami tungowymi , kamforowcami , wawrzynami i zaroślami bambusowymi .
Na wybrzeżu Morza Południowochińskiego i przybrzeżnych wyspach występują wiecznie zielone lasy zwrotnikowe , bogate w palmy , bambusy , liany i epifity .
Wyżyny i kotliny północnych i północno-zachodnich Chin porastają stepy – na wschodzie bujne łąkowe na zachodzie suche z kserofilnymi gatunkami ostnic i bylic .
