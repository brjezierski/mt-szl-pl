Język chiński
Język chiński ( lub ; pinyin `` Zhōngwén '' ) – język lub grupa spokrewnionych języków ( w klasyfikacji ISO 639-3 : makrojęzyk ) z rodziny języków chińsko-tybetańskich .
W piśmie chińskim zazwyczaj nie oddaje się różnic w wymowie ( różnych tonów tej samej sylaby ) , tj .
zapis jest identyczny , ale jego wymowa może się bardzo różnić w zależności od mowy danego użytkownika .
Poszczególne odmiany językowe mogą być nawet wzajemnie niezrozumiałe .
`` Ethnologue '' wyróżnia w obrębie makrojęzyka kilkanaście języków chińskich .
Standardowy język mandaryński .
Standardowy język chiński , nazywany przez zwolenników istnienia wielu języków chińskich standardowym językiem mandaryńskim , to oficjalny język Chińskiej Republiki Ludowej , opracowany w latach 50 .
XX w. i nauczany w chińskich szkołach od 1956 roku .
Według danych spisu powszechnego z 2000 roku , w języku tym mówi w Chinach ponad 840 mln osób .
Liczbę cudzoziemców władających tym językiem lub uczących się go władze chińskie szacują na 30 mln osób .
