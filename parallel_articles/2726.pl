Czubajnik ogrodowy
Czubajnik ogrodowy ( `` Chlorophyllum brunneum '' ( Farl .
& amp ; Burt ) Vellinga ) – gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chlorophyllum , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1929 r. William Gilson Farlow i Edward Angus Burt nadając mu nazwę `` Lepiota brunnea '' .
Obecną , uznaną przez Index Fungorum nazwę nadała mu w 2002 r. Else Vellinga , przenosząc go do rodzaju `` Chlorophyllum '' .
Polską nazwę czubajnik ogrodowy w 2015 r. zaproponowała grupa mykologów w publikacji Karasińskiego i in. , a jej używanie Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała w 2021 r .
