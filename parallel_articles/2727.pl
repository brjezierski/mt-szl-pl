Chlorophyllum molybdites
Chlorophyllum molybdites ( G .
Mey . )
Massee ex P. Syd .
– gatunek grzybów należący do rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chlorophyllum , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1818 r. Georg Friedrich Wilhelm Meyer nadając mu nazwę `` Agaricus molybdites '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1900 r. G.E .
Massee i Paul Sydow przenosząc go do rodzaju `` Chlorophyllum '' .
Synonimy naukowe :
Atlas grzybów podaje polską nazwę sinoblaszek trujący .
