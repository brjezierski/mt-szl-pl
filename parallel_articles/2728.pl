Czubajnik ponury
Czubajnik ponury ( `` Chlorophyllum olivieri '' ( Barla ) Vellinga ) – gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chlorophyllum , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1886 r. Jean-Baptiste Barla nadając mu nazwę `` Lepiota olivieri '' .
Obecną , uznaną przez Index Fungorum nazwę nadała mu w 2002 r. Else Vellinga , przenosząc go do rodzaju `` Chlorophyllum '' .
Synonimy naukowe według Index Fungorum .
Polską nazwę czubajnik ponury w 2015 r. zaproponowała grupa mykologów w publikacji Karasińskiego i in. , a jej używanie Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała w 2021 r .
