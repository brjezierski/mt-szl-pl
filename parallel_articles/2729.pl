Chlorophyllum sphaerosporum
Chlorophyllum sphaerosporum Z.W .
Ge & amp ; Zhu L. Yang – gatunek wielkoowocnikowych gtrzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten został po raz pierwszy opisany przez Zai Wei Ge i Zhu Liang Yanga w artykule „ The genus Chlorophyllum ( Basidiomycetes ) in China ” opublikowanym w `` Mycotaxonie '' z 2006 :
Charakterystyka .
Wytwarza naziemne , kapeluszowe owocniki o blaszkowatym hymenoforze .
Kapelusz 4,5–6,5 cm średnicy , biały lub białawy , pokryty brązowawymi łuseczkami i o lekko żłobkowanym brzegu .
blaszki wolne , gęsto rozmieszczone , białe ( wyschnięte – kremowe ) .
Trzon 4–7 cm długości , 0,5–0,7 cm średnicy , prawie cylindryczny , białawy .
Pierścień błoniasty , białawy , ruchomy .
Miąższ biały , niezmienny .
Charakterystyczne dla tego gatunku są małe ( typ .
9.0-10.0×7.0-9.0μm ) , kulistawe , gładkie , hialinowe , dekstrynoidalne i pozbawione pory rostkowej zarodniki .
Występowanie .
Gatunek ten odkryto w chińskich lasach , wśród keteleerii ( `` Keteleeria '' ) i kasztanków ( `` Xanthoceras '' ) .
