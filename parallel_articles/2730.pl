Chlust
Chlust – szybka , wieloosobowa , hazardowa gra karciana , popularna wśród szkolnej młodzieży na terenie Górnego Śląska oraz okolic Radomia w czasach PRL-u .
Nazwa gry wzięła się od onomatopei oznaczającej odgłos , jaki wydawały karty przy nerwowym rzucaniu ich na stół bądź ziemię przez graczy .
Jest nazywana również śląskim pokerem .
Zasady gry .
Gra składa się z trzech etapów :
Do gry potrzebna jest talia 20 kart ( od asa do dziesiątek ) .
Celem gry jest zdobycie tytułowego chlusta .
Jeżeli nikt nie będzie miał chlusta , wygrywa ten , kto zdobył więcej lew .
Chlust .
Chlust to 3 karty tego samego koloru lub tej samej figury .
Dziesiątka trefl może zastąpić dowolną kartę , ale tylko w 2 fazie gry , przy zgłaszaniu chlustów ; nigdy podczas zbierania lew .
Jeżeli kartą pod stosem jako atut jest właśnie dziesiątka trefl , to jej funkcję przejmuje dziesiątka pik .
Chlusta bez dziesiątek zastępujących karty nazywamy chlustami czystymi , które są wyżej w starszeństwie kart niż nieczyste .
Gdy dziesiątka trefl jest w sekwensie z dwoma kartami treflowymi , to jest to chlust czysty , ponieważ nie ma potrzeby zastępowania .
W tym przykładzie funkcję dziesiątki trefl dla tego gracza przejmuje dziesiątka pik .
Rozdanie kart .
W chlusta gra się symbolicznie .
Każda osoba musi wyłożyć „ ceskiego ” , czyli 10 groszy .
Uczestnicy gry wybierają rozdającego , który rozdaje wszystkim graczom po 3 karty .
Rozdający ma obowiązek zaoferowania przełożenia talii graczowi po jego prawej stronie .
Po tym odkrywa jedną kartę i kładzie ją pod stosem awersem do góry tak , aby jej wartość oraz kolor był widoczny dla wszystkich .
Kolor tej karty określa atut .
Jeżeli odkrytą w ten sposób kartą jest as to gracz , jeśli ma dziesiątkę karo bądź kier , może ją wymienić na znajdującego się pod stosem asa .
Licytacja .
Po rozdaniu , gdy któryś z graczy uzna , że ma chlusta bądź dobre karty , dokłada kolejnego ceskiego do puli .
Pozostali muszą wówczas dołożyć tyle samo , jeżeli chcą dalej grać i nie spasują .
Dokłada się ceskie do chwili , w której nikt nie będzie już chciał zwiększyć puli .
Wówczas gdy ktoś ma chlusta , pokazuje go innym .
Jeżeli więcej niż jedna osoba ma chlusta , to trzeba je porównać według schematu znajdującego się poniżej .
Jeżeli nikt nie miał chlusta , następuje gra ze zbieraniem lew .
Starszeństwo chlustów .
4 .
3 te same figury ( w przypadku remisu wygrywa ten , kto ma w nim silniejsze karty ) .
W tym przypadku dziesiątki również liczą się do figur .
Jeżeli mamy do porównania chlust czysty ( bez dziesiątki trefl bądź pik ) i chlust nieczysty ( z dziesiątkami trefl bądź pik ) , to zawsze starszym chlustem będzie chlust czysty .
Zbieranie lew .
Gdy po rozdaniu żaden z graczy nie będzie miał chlusta , to rozdający rozpoczyna grę kładąc kartę z ręki na stół rozpoczynając lewę .
Gracze muszą wykładać karty zgodnie z następującymi zasadami :
Lewę wygrywa gracz , który wystawił najmocniejszą kartę w kolorze karty początkującej bądź atutu .
Wówczas zabiera on karty z lewej do siebie na osobny stosik i wykłada kartę rozpoczynającą kolejną lewę .
Koniec rozdania .
Gdy wszyscy rozegrają ostatnią lewę , każdy podlicza liczbę lew , które udało mu się wziąć .
Ten , kto zdobył ich najwięcej , wygrywa rozdanie i bierze pulę .
W przypadku remisu gracze dokładają do puli kolejne ceskie i rozgrywają nowe , decydujące rozdanie .
Następnym rozdającym jest osoba znajdująca się po lewej stronie poprzedniego .
Gra się dowolną liczbę rozdań , jednakże powinno się grać tak , aby gracze postanowili zakończyć grę po partii , w której rozdającym był gracz znajdujący się po prawej stronie początkowego .
Dodatkowe zasady .
Dołożenie 5 złotych .
Jeżeli ktoś ma dziesiątkę trefl ( bądź w wymienionym powyżej przypadku dziesiątkę pik ) , może , trzymając ją w ręce z pozostałymi kartami , odwrócić ją tak , aby awers był widoczny dla pozostałych graczy .
Wówczas może dołożyć do puli 5 zł i gra jest toczona dalej .
