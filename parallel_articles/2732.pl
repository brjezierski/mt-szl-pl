Chlewiska ( osada leśna w województwie wielkopolskim )
Chlewiska – osada leśna w Polsce położona w województwie wielkopolskim , w powiecie szamotulskim , w gminie Kaźmierz .
Pod koniec XIX osada miała status kolonii i liczyła 2 domostwa z 18 mieszkańcami ( 15 katolików i 3 wyznania ewangelickiego ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
