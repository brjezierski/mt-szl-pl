Chmielnicki
Chmielnicki ( ukr .
Хмельницький , `` Chmelnyćkyj '' ; do 1954 Płoskirów , ukr .
Проскурів , `` Proskuriw '' ) – miasto w zachodniej części Ukrainy , nad Bohem , stolica obwodu i rejonu .
Miasto liczy 273 713 mieszkańców ( 2020 ) , dla porównania spis powszechny w 2001 zanotował ich 251 077 .
Około 100 km na północ od miasta znajduje się elektrownia jądrowa o mocy 2 GW .
Historia .
W czasach I Rzeczypospolitej nosiło nazwę Płoskirów i wchodziło początkowo w skład dóbr królewskich .
Leżało w granicach województwa podolskiego prowincji małopolskiej Korony Królestwa Polskiego .
