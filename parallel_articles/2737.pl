Chmielnik ( Kierpień )
Chmielnik – przysiółek wsi Kierpień w Polsce , położony w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Przepływa przez niego rzeka Osobłoga .
W latach 1975–1998 przysiółek administracyjnie należał do ówczesnego województwa opolskiego .
Nazwa .
Nazwa `` Chmielnik '' oznacza w języku polskim miejsce uprawy chmielu .
W historycznych dokumentach nazwę miejscowości wzmiankowano w języku niemieckim w formach : `` Birkenmühle '' ( 1736 ) , `` Körpener Mühle '' ( 1750 ) , `` Chmielnik-M. '' ( 1931 ) .
`` Birkenmühle '' było oficjalną nazwą używaną przez niemieckojęzycznych cystersów , natomiast nazwy `` Młyn Chmielniczki '' używała ludność śląska .
