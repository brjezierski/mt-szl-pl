Chociebórz
Chociebórz ( niem .
`` Koschpendorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Kamiennik .
Liczba mieszkańców około 140 osób .
Zabytki .
Granica św .
Jana – słup graniczny – słup biskupi : stoi po lewej stronie polnej drogi prowadzącej z Chocieborza do Osiny Wielkiej .
Jest to dawna granica księstwa nyskiego .
Granicę tę oznakowali granitowymi słupami .
Postawiono je po zakończeniu sporu o uznanie biskupiej zwierzchności nad ziemią nyską , w którym stronami byli książę Bolko I Surowy i biskup wrocławski Jan III Romka .
Do dzisiaj zachowało się jedynie 6 takich słupów .
Są to najstarsze znaki graniczne znane na polskich ziemiach .
Wyryto na nich napis `` TMI SCI IOHIS '' , co w języku polskim oznacza `` Granice świętego Jana '' .
Każdy słup ma na jednej ze ścian bocznych wyryty pastorał , symbol biskupi , a na przeciwległej ― książęcy znak ukośnego krzyża .
Monolitowy kamienny krzyż nieznanego pochodzenia i wieku , być może późnośredniowieczny , przy skrzyżowaniu dróg polnych , ok. 80 m od słupa granicznego św .
