Chodecz
Chodecz – miasto w Polsce położone w województwie kujawsko-pomorskim , w powiecie włocławskim , siedziba gminy miejsko-wiejskiej Chodecz .
Leży na Pojezierzu Kujawskim , w obrębie Wysoczyzny Kujawskiej .
Chodecz uzyskał lokację miejską w 1442 roku , zdegradowany po 1800 roku , ponowne nadanie praw miejskich w 1822 roku , degradacja w 1870 roku , nadanie praw miejskich w 1921 roku .
W latach 1975–1998 miasto administracyjnie należało do województwa włocławskiego .
W czasie okupacji hitlerowskiej miasto nosiło nazwę Godetz .
Według danych GUS z 31 grudnia 2019 r. Chodecz liczył 1852 mieszkańców .
Prywatne miasto szlacheckie lokowane w 1442 roku położone było w XVI wieku w województwie brzeskokujawskim .
Miasto prywatne Królestwa Kongresowego , położone było w 1827 roku w powiecie kowalskim , obwodzie kujawskim województwa mazowieckiego .
W miejscowości funkcjonuje kąpielisko ( plaża miejska ) .
