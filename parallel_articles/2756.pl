Chorula
Chorula ( dodatkowa nazwa w j . )
– wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Gogolin , nad Odrą , przy drodze z Krapkowic do Opola .
Na terenie wioski funkcjonują : sklep , bar oraz Centrum Aktywizacji Wiejskiej , w którym znajduje się biblioteka .
Poczta dla Choruli znajduje się w Górażdżach , a ośrodek zdrowia w Malni .
Nazwa .
W 1295 w kronice łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) wymieniająca miejscowości płacące dziesięcinę wieś wymieniona jest jako `` Chorula '' we fragmencie `` Chorula decima more polonico '' .
Wzmiankowana w 1306 roku jako `` Chorula villa '' .
W latach 1934–1945 miejscowość nosiła nazwę `` Steinfurt '' .
Historia .
W 1783 roku liczyła 115 mieszkańców .
