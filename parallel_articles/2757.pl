Język chorwacki
Język chorwacki ( chorw . )
– język indoeuropejski z grupy zachodniej języków południowosłowiańskich .
Jest używany przez Chorwatów , zamieszkujących Chorwację , Bośnię i Hercegowinę , serbską Wojwodinę i sąsiednie kraje .
Jest językiem urzędowym Chorwacji , a także jednym z trzech języków urzędowych Bośni i Hercegowiny .
W pobliskich krajach bałkańskich , takich jak Serbia i Czarnogóra , chorwacki występuje jako język mniejszościowy .
Łącznie jako mową ojczystą posługuje się nim ok. 5,5 mln osób .
Status języka chorwackiego i kwestia jego odrębności od języka serbskiego stanowi przedmiot kontrowersji politycznej .
Oba języki , na poziomie form literackich , są do siebie zbliżone i zachowują dobrą wzajemną zrozumiałość , przy czym serbski charakteryzuje się przywiązaniem do prawosławia i cyrylicy , a chorwacki jest kojarzony z katolicyzmem i pismem łacińskim .
Różnice między językami uwidaczniają się na płaszczyźnie słownictwa – serbski i chorwacki wykazują różne preferencje leksykalne , co wynika m.in .
z wpływów różnych języków i tym samym odrębnych źródeł zapożyczeń .
Najbardziej wyraźne podziały językowe istnieją jednak nie w ramach standardów o zasięgu ogólnonarodowym , lecz na poziomie bardziej zróżnicowanych gwar miejscowych .
Z punktu widzenia typologii lingwistycznej chorwacki jest językiem fleksyjnym .
W zdaniu przeważa szyk SVO ( podmiot orzeczenie dopełnienie ) , przy czym porządek wyrazów jest stosunkowo swobodny .
W języku standardowym istnieje siedem przypadków , do których dołącza się przyimki .
