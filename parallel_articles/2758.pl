Chorwacja
Chorwacja , Republika Chorwacji ( , ) – państwo leżące na pograniczu Europy Środkowej i Europy Południowej , nad Morzem Adriatyckim i graniczące od południa z Bośnią i Hercegowiną oraz Czarnogórą , od wschodu z Serbią oraz Węgrami i Słowenią od północy .
Od południowego zachodu ma dostęp do Morza Adriatyckiego .
Od 1 lipca 2013 należy do Unii Europejskiej jako 27. członek wspólnoty ( wcześniej 28 . ) .
Od 1 stycznia 2023 w strefie euro i Schengen .
Historia .
Do I wieku n.e .
na terenie Chorwacji znajdowała się rzymska prowincja Iliria ( łac .
`` Illyricum '' ) .
W I wieku n.e .
została ona podzielona na dwie prowincje : Dalmację i Panonię .
Na początku VII wieku na tereny dzisiejszej Chorwacji przybyły plemiona Chorwatów .
W 803 roku tereny Chorwacji Dalmatyńskiej zostały podbite i włączone do imperium Karola Wielkiego .
Od VII do VIII wieku najeżdżały je plemiona Ostrogotów , Awarów i Słowian .
W późniejszym czasie tereny Chorwacji stały się miejscem rywalizacji o wpływy : papiestwa , Wenecji i Węgier .
W 925 powstało Królestwo Chorwacji .
W 1102 doszło do unii personalnej z Węgrami .
Od XIV wieku i końca panowania Andegawenów na Węgrzech ( 1308–1395 ) nastąpił okres walk wewnętrznych .
