Chorzepin
Chorzepin – wieś w Polsce położona w województwie łódzkim , w powiecie łęczyckim , w gminie Świnice Warckie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa konińskiego .
Wieś leży w odległości 8 km na północny zachód od Świnic Warckich .
Przez wieś przepływa rzeka Ner .
Pierwsza informacja o miejscowości pochodzi z 1372 .
Była to wieś szlachecka .
We wsi znajdował się wiatrak drewniany ( koźlak ) z 1665 ( obecnie jest w Uniejowie ) oraz cmentarz ewangelicki , którego rudymenty zachowały się do dnia dzisiejszego .
Sołtysem jest Wioletta Urbańska .
Z miejscowości pochodzi piosenkarka Ewa Śnieżanka .
