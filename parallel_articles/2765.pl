Choszczno ( stacja kolejowa )
Choszczno – stacja kolejowa w Choszcznie , w województwie zachodniopomorskim , w Polsce .
W roku 2018 stacja obsługiwała ok. 1500 pasażerów na dobę .
Stacja znajduje się przy ulicy Kolejowej 1 .
Niedawno budynek dworca znajdował się w fatalnym stanie technicznym .
W 2016 roku wyremontowano poczekalnię .
