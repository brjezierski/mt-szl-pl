Christian Wulff
Christian Wilhelm Walter Wulff ( wym .
; ur .
19 czerwca 1959 w Osnabrück ) – niemiecki prawnik i polityk , działacz Unii Chrześcijańsko-Demokratycznej , premier Dolnej Saksonii w latach 2003–2010 .
Od 30 czerwca 2010 do 17 lutego 2012 prezydent Republiki Federalnej Niemiec .
Życiorys .
Wykształcenie i praca zawodowa .
Ukończył studia prawnicze na Uniwersytecie w Osnabrück .
Zdał następnie egzaminy państwowe i rozpoczął praktykę w zawodzie adwokata .
Działalność polityczna do 2010 .
W działalność polityczną zaangażowany od końca lat 70. , kiedy to wstąpił do CDU .
Od 1983 do 1985 był przewodniczącym chadeckiej organizacji młodzieżowej Junge Union w Dolnej Saksonii .
