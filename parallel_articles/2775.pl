Złotopępówka różowawa
Złotopępówka różowawa ( `` Chromosera cyanophylla '' ( Fr . )
Redhead , Ammirati & amp ; Norvell – gatunek grzybów należący do rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chromosera , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1861 r. Elias Fries nadając mu nazwę `` Agaricus cyanophyllus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2011 r. Redhead , Ammirati & amp ; Norvell , przenosząc go do rodzaju `` Chromosera '' .
