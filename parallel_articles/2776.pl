Klejek alpejski
Klejek alpejski ( `` Chroogomphus helveticus '' ( Singer ) M.M .
Moser – gatunek grzybów z rodziny klejówkowatych ( `` Gomphidiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chroogomphus , Gomphidiaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1770 r. R. Singer nadając mu nazwę `` Gomphidius helveticus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1967 r. M.M .
Moser , przenosząc go do rodzaju `` Chroogomphus '' .
Synonimy naukowe
Alina Skirgiełło w 1960 r. opisywała ten gatunek pod nazwą klejówka helwecka , w 1983 r. Barbara Gumińska i Władysław Wojewoda zmienili nazwę na klejek alpejski .
Morfologia .
Średnicy od 4 do 8 cm .
