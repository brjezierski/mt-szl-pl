Gerronema strombodes
Gerronema strombodes ( Berk .
& amp ; Mont . )
Singer ( pępnica szarobrązowa ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gerronema , Marasmiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten został opisali w 1856 r. przez Miles Joseph Berkeley i Camille Montagne jako `` Agaricus strombodes '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Rolf Singer w 1961 r .
