Nazwę polską zaproponował Władysław Wojewoda w 2003 r. , wówczas gatunek ten zaliczany był do rodzaju `` Chrysomphalina '' ( pępnica ) .
Po przeniesieniu go do rodzaju `` Gerronema '' nazwa polska jest niespójna z nazwą naukową i myląca , tym bardziej , że obydwa gatunki zaliczane są obecnie do innych rodzin .
Morfologia .
Średnica 1,5–4 cm , początkowo łukowaty z niewielką wklęsłością , potem silnie lejkowaty .
Powierzchnia jedwabista i matowa , początkowo promieniście włóknista i szara do czerwonobrązowej , potem jaśniejsza – kremowa lub ochrowobrązowa , a jej promieniste włókna stają się szarobrązowe .
