Chrysomyxa abietis
Chrysomyxa abietis ( Wallr . )
Unger – gatunek grzybów z rodziny pęcherzycowatych ( `` Coleosporiaceae '' ) .
Grzyb mikroskopijny pasożytujący na świerkach .
Wywołuje u nich chorobę o nazwie rdza złotawa świerka .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chrysomyxa , Coleosporiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1834 r. Karl Friedrich Wallroth nadając mu nazwę `` Blennoria abietis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Franz Unger w 1840 r. Synonimy :
Morfologia i rozwój .
Grzyb pasożytniczy .
Jest pasożytem jednodomowym , gdyż cały jego cykl rozwojowy odbywa się na jednym żywicielu .
