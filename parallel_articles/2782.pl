Chrysomyxa empetri
Chrysomyxa empetri ( Pers . )
J. Schröt .
– gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny pasożytujący na świerkach i bażynie .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chrysomyxa , Coleosporiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1815 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo empetri '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Joseph Schröter w 1887 r .
Synonimy :
Morfologia i cykl życiowy .
Pasożyt dwudomowy , którego pełny cykl życiowy odbywa się na dwóch gatunkach żywicieli .
Na igłach świerków tworzą się żółte ecja .
Igły nie ulegają zniekształceniu , ale poprzecznie powstają na nich szerokie , żółte strefy .
Przy dużej ich ilości całe igły żółkną .
