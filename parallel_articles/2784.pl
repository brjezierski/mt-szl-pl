Chrzanów
Chrzanów – miasto w Polsce położone w województwie małopolskim , w powiecie chrzanowskim , siedziba władz powiatu chrzanowskiego i gminy miejsko-wiejskiej Chrzanów .
Miasto ma mieszkańców ( 31 grudnia 2019 ) .
Chrzanów uzyskał lokację miejską około 1350 roku .
Położenie .
Miasto położone jest nad rzeką Chechło ( lewym dopływem Wisły ) .
Chrzanów leży na utworach Garbu Tenczyńskiego i Rowu Krzeszowickiego – południowej części Wyżyny Krakowsko-Częstochowskiej .
Niewielka zachodnia część gminy znajduje się w granicach Wyżyny Śląskiej Pagórów Jaworznickich .
Chrzanów zaliczany jest do Krakowskiego Okręgu Przemysłowego .
