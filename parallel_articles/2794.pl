Chwarzno ( Gdynia )
Chwarzno ( kaszb .
`` Chwôrzno '' ) – zwyczajowa dzielnica i osiedle położone w zachodniej części Gdyni , stanowiące część dzielnicy Chwarzno-Wiczlino .
Nazwa „ Chwarzno ” pochodzi od określeń `` krzak '' , `` zagajnik '' , `` chrust '' oraz `` teren pagórkowaty '' .
Wieś powstała w średniowieczu w wyniku połączenia dwóch sąsiadujących ze sobą osad : Skrobotowa i zachodniej części Witomina .
Była to wieś folwarczna , z tamtego okresu zachowało się w niej kilka budynków mieszkalnych i gospodarczych oraz odnowiony w 2015 roku teren dawnego parku przydworskiego .
Obecnie jest to typowe podmiejskie osiedle mieszkalne o zwartej zabudowie , w której skład wchodzą zarówno domy jednorodzinne , jak i kilkukondygnacyjne bloki mieszkalne .
