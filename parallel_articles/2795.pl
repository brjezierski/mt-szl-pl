Chwaszczyno ( gmina )
Chwaszczyno ( od 1973 `` Kielno '' ) – dawna gmina wiejska istniejąca w latach 1934-1954 w woj .
pomorskim/gdańskim ( dzisiejsze woj .
pomorskie ) .
Siedzibą władz gminy było Chwaszczyno .
Gmina zbiorowa Chwaszczyno została utworzona 1 sierpnia 1934 roku w powiecie morskim w woj .
pomorskim ( II RP ) z dotychczasowych jednostkowych gmin wiejskich : Bojano , Chwaszczyno , Dobrzewino , Kielno , Koleczkowo , Kolonja , Osowa , Wiczlino i Wielki Kack oraz z obszarów dworskich położonych na tych terenach lecz nie wchodzących w skład gmin .
Po wojnie ( 7 kwietnia 1945 roku ) gmina wraz z całym powiatem morskim weszła w skład nowo utworzonego woj .
gdańskiego .
1 lipca 1951 roku zmieniono nazwę powiatu morskiego na `` wejherowski '' .
Według stanu z dnia 1 lipca 1952 roku gmina składała się z 9 gromad : Bojano , Chwaszczyno , Dobrzewino , Kielno , Koleczkowo , Kolonia , Osowa , Wiczlino i Wielki Kack .
12 września 1953 roku część obszaru gminy Chwaszczyno ( gromadę Wielki Kack ) przyłączono do Gdyni .
Gmina została zniesiona 29 września 1954 roku wraz z reformą wprowadzającą gromady w miejsce gmin .
Jednostki nie przywrócono 1 stycznia 1973 roku po reaktywowaniu gmin , utworzono natomiast jej terytorialny odpowiednik , gminę Kielno .
