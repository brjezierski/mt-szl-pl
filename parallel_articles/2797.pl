( m.in .
Kri , Odżibwejowie ) sny nawiedzające śpiących w domu musiały przejść przez amulet .
Gęsta sieć miała przepuszczać jedynie dobre sny , a zatrzymywać nocne koszmary , które ginęły wraz z pierwszymi promieniami słońca .
Taki sen miał łapać się w sieć i spływać po piórach .
Współcześnie łapacz snów jest popularną pamiątką często kupowaną przez turystów w sklepach i na straganach z indiańskimi pamiątkami ( choć wiele tanich imitacji łapaczy produkowanych jest obecnie w krajach Dalekiego Wschodu ) .
W Ameryce Północnej ( a czasem i na innych kontynentach ) popularne stało się wieszanie łapaczy snów jako mających przynosić szczęście amuletów np .
w pokojach dziecięcych lub na lusterkach wstecznych w samochodach .
