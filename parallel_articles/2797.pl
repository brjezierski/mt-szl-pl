Łapacz snów
Łapacz snów ( ang .
`` dreamcatcher '' lub `` dream catcher '' ) – amulet niektórych plemion Indian z Ameryki Północnej .
Dziś , w wersji komercyjnej , najczęściej składa się z sieci wplecionej w okrąg wykonany ze sprężystego drewnianego pręta ( np .
wierzby ) z trzema piórami .
Prawdziwe łapacze snów były w kształcie łezki lub rybki , bez żadnych prętów , tylko ze splecionych nici oraz z jednym piórem .
Łapacz , jako wytwór rąk rodziców , miał być dla dziecka łącznikiem z ich światem .
Wewnątrz ramy wpleciona jest schodząca się spiralnie ku środkowi sieć ze ścięgna , włosia lub rzemienia .
Amulet przyozdobiony jest zwykle piórami , paciorkami i innymi ozdobami , którym Indianie przypisywali określone właściwości magiczne .
Łapacze snów zwykle wieszane były nad posłaniem lub przy wejściu do domostwa indiańskiego .
