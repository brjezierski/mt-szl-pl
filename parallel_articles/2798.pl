Châlons-en-Champagne
Châlons-en-Champagne ( do 1998 Châlons-sur-Marne , łac .
`` Catalaunum '' ) – miasto i gmina w Szampanii w północnej Francji , stolica departamentu Marna .
Według danych na 2016 populacja miasta wynosiła 44 980 osób , a gęstość zaludnienia – 1727 osób/km² .
Populacja całej aglomeracji w 2015 wynosiła 81 858 osób .
Historia .
Wielu historyków uważa , że bitwa na Polach Katalaunijskich odbyła się w pobliżu tego miasta w 451 r. Bitwa toczyła się między rzymskim wodzem Aecjuszem , któremu towarzyszyli Wizygoci pod wodzą swego króla Teoderyka , a Hunami dowodzonymi przez ich króla Attylę z ich sojusznikami .
W latach 1897–1938 w mieście działała sieć tramwajowa .
W latach 1956–2015 Châlons-en-Champagne było stolicą regionu Szampania-Ardeny .
