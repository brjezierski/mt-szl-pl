Château-Gontier
Château-Gontier – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Mayenne .
W 2013 roku populacja ówczesnej gminy wynosiła 12 348 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia trzech ówczesnych gmin – Azé , Château-Gontier oraz Saint-Fort – powstała nowa gmina Château-Gontier-sur-Mayenne .
Siedzibą gminy została miejscowość Château-Gontier .
