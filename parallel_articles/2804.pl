Châtelet ( stacja metra )
Châtelet – stacja linii 1 , 4 , 7 , 11 i 14 metra w Paryżu , położona na granicy 1. i 4. dzielnicy .
W przypadku linii 11 jest to stacja końcowa .
Stacja .
Stacja została uruchomiona w 1900 roku , na linii 1 .
W 1908 roku została połączona z nowo wybudowaną stacją linii 4 .
