Hohhot
Hohhot ( chiń .
呼和浩特 , pinyin : `` Hūhéhàotè '' ; mong .
ᠬᠥᠬᠡᠬᠣᠲᠠ , `` Kökeqota '' ; Хөх хот , `` Chöch chot '' ) – miasto o statusie prefektury miejskiej w północnych Chinach , ośrodek administracyjny regionu autonomicznego Mongolia Wewnętrzna , w dolinie rzeki Huang He , u podnóży Daqing Shan .
W 2010 roku liczba mieszkańców miasta wynosiła 802 403 .
Prefektura miejska w 1999 roku liczyła 2 115 432 mieszkańców .
Ośrodek gospodarki i centrum kultury Mongołów w Chinach ; rozwinięty przemysł skórzany , włókienniczy , odzieżowy , spożywczy , maszynowy oraz rzemiosło artystyczne .
W mieście działa kilka szkół wyższych , w tym uniwersytet .
Siedziba rzymskokatolickiej archidiecezji Suiyuan .
14 km na wschód od Hohhotu znajduje się port lotniczy Hohhot-Baita .
Miasto nazywane jest przez Mongołów „ Niebieskim Miastem ” , a przez Hanów „ Zielonym Miastem ” ( 青城 , `` Qīng Chéng '' ) .
Historia .
Miasto zostało założone w XVI w. przez Mongołów i było ważnym ośrodkiem buddyzmu tybetańskiego .
W drugiej połowie XVII w. , pod koniec panowania dynastii Ming , obszar zaczęli zasiedlać Chińczycy , którzy trudnili się przede wszystkim rolnictwem .
Nadali oni miastu nazwę Guihua ( 歸化 ) .
