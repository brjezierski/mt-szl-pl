Chądzyń
Chądzyń – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Sterdyń .
Obok miejscowości przepływa rzeczka Buczynka , dopływ Bugu .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Mieszkańcy wyznania rzymskokatolickiego należą do parafii św .
Anny w Sterdyni .
