Kubianka kotkowa
Kubianka kotkowa ( `` Ciboria amentacea '' ( Balb . )
Fuckel ) – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Ciboria , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1804 r. E.G .
Balbiani nadając mu nazwę `` Peziza amentacea '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Leopold Fuckel w 1870 r .
