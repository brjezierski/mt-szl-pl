Kubianka leszczynowa
Kubianka leszczynowa ( `` Ciboria coryli '' ( Schellenb . )
N.F .
Buchw . )
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Ciboria , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1906 r. Hans Conrad Schellenberg nadając mu nazwę `` Sclerotinia coryli '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Niels Fabritius Buchwald w 1943 r .
