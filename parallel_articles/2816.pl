Kubianka rudawa
Kubianka rudawa ( `` Ciboria rufofusca '' ( O .
Weberb . )
Sacc . )
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Ciboria , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1873 r. Otto Weberbauer nadając mu nazwę `` Peziza rufofusca '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Pier Andrea Saccardo w 1889 r .
Polska nazwa według M.A .
Chmiel .
Morfologia .
