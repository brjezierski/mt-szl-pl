Ciboria viridifusca
Ciboria viridifusca ( Fuckel ) Höhn .
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Ciboria , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1870 r. Leopold Fuckel nadając mu nazwę `` Peziza viridifusca '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Franz Xaver Rudolf von Höhnel w 1926 r .
