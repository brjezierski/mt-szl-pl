Ciborinia allii
Ciborinia allii ( Sawada ) L.M .
Kohn – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Gatunek szeroko rozprzestrzeniony .
Poza Antarktydą występuje na wszystkich kontynentach .
Wraz z kilku innymi patogenami wywołuje u nich chorobę o nazwie zgnilizna szyjki cebuli
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Botrytinia , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1919 r. K. Sawada nadając mu nazwę `` Sclerotinia allii '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu L.M .
Kohn r. w 1979 r .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Pasożyt i saprotrof występujący w tkankach porażonych roślin i na ich obumarłych resztkach .
