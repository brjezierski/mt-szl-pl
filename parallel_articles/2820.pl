Ciesznów
Ciesznów ( , ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Doliny Górnej Straduni , będącego częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 80 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 11 km od granicy z Czechami , w Dolinie Górnej Straduni , tuż przy granicy powiatu prudnickiego z powiatem kędzierzyńsko-kozielskim ( gmina Pawłowiczki ) i powiatem głubczyckim ( gmina Głubczyce ) .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Środowisko naturalne .
W Ciesznowie panuje klimat umiarkowany ciepły .
Średnia temperatura roczna wynosi +8,2 °C .
Duże zróżnicowanie dotyczy termicznych pór roku .
