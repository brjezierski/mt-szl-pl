( 1373 ) Cincinnati – planetoida należąca do zewnętrznej części pasa głównego asteroid okrążająca Słońce w ciągu 6 lat i 114 dni w średniej odległości 3,41 au .
Została odkryta 30 sierpnia 1935 roku w Mount Wilson Observatory przez Edwina Hubble ' a .
Nazwa planetoidy pochodzi od `` Cincinnati Observatory '' .
Przed jej nadaniem planetoida nosiła oznaczenie tymczasowe ( 1373 ) 1935 QN .
