Cintractia majewskii
Cintractia majewskii Piątek & amp ; Vánky – gatunek grzybów należący do rzędu głowniowców ( `` Ustilaginales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Anthracoideaceae , Ustilaginales , Ustilaginomycetidae , Ustilaginomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Gatunek ten został po raz pierwszy opisany przez Marcina Piątka i Kálmána Vánky'ego w artykule `` Cintractia majewskii , a new smut fungus ( Ustilaginomycetes ) on Fimbristylis ( Cyperaceae ) from Africa '' , opublikowanym w „ Polish Botanical Journal ” z 2005 :
Nazwa gatunkowa została nadana na cześć polskiego mikologa , Tomasza Majewskiego .
Charakterystyka .
Grzyby z gatunku `` Cintractia majewskii '' rozwijają się w kłoskach roślin z rodzaju `` Fimbristylis '' – wiechlinowca należącego do rodziny ciborowatych ( `` Cyperaceae '' ) .
Owocniki tego gatunku wytwarzane są w postaci `` sori '' , pojawiających się na kwiatach żywiciela .
Są to kulistawe , czarne twory o średnicy 0,5–1mm , złożonego ze zlepionych zarodników .
`` Sori '' , podobnie jak u `` Cintractia amazonica '' , pozbawione są jednak otaczającego je perydium , co jest charakterystyczne raczej dla rodzaju `` Ustanciosporium '' .
Występowanie .
Gatunek ten jest znany jedynie z próbek roślinnych , zebranych w 1914 przez jezuickiego misjonarza , Hyacinthe ' a Vanderysta w Kongu .
