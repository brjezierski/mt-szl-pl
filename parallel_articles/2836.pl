Cytryna zwyczajna
Cytryna zwyczajna `` Citrus limon '' ( L. ) Burm. , nazywana też cytryną właściwą lub po prostu cytryną – gatunek roślin z rodziny rutowatych ( `` Rutaceae '' Juss . ) .
Pochodzi z południowo-wschodnich Chin , lecz współcześnie nie występuje tam ani w stanie dzikim , ani nie jest tam uprawiana .
Prawdopodobnie powstała w wyniku skrzyżowania limy ( `` Citrus aurantifolia '' syn .
`` C. latifolia '' ) z cytronem ( `` Citrus medica '' ) .
Do Europy trafiła w średniowieczu ( starożytni Rzymianie znali i uprawiali tylko cytrony `` Citrus medica '' ) .
