Ciudad Real ( prowincja )
Ciudad Real – region administracyjny we wspólnocie autonomicznej Kastylia-La Mancha w Hiszpanii ze stolicą w Ciudad Real .
Od północy graniczy z prowincją Toledo , na północnym wschodzie z prowincją Cuenca , na wschodzie z prowincją Albacete , na południowym wschodzie z prowincją Jaén , na południowym zachodzie z prowincją Cordoba oraz na zachodzie z prowincją Badajoz .
Liczy 497 691 mieszkańców ( 2005 ) i zajmuje 19 813 km² .
