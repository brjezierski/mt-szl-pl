Cizre
Cizre − miasto w Turcji w prowincji Şırnak .
Według danych na rok 2011 miasto zamieszkiwało 104 844 osób .
W mieście znajduje się oddany do użytku w 1998 stadion `` Cizre Ilce Stadi '' , na którym swoje mecze rozgrywa drużyna piłkarska Cizrespor .
Stadion może pomieścić 2 400 widzów .
W mieście został stracony syryjski biskup Flawian Michał Melki .
20 lipca 2021 roku w mieście odnotowano najwyższą w historii kraju temperaturę w cieniu 49.1°C .
