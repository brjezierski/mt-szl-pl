Chrobotek leśny
Chrobotek leśny , chrobotka leśna ( `` Cladonia arbuscula '' ( Wallr . )
Flot . )
– gatunek grzybów należący do rodziny chrobotkowatych ( `` Cladoniaceae '' ) .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cladonia , Cladoniaceae , Lecanorales , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1829 r. Carl Friedrich Wallroth nadając mu nazwę `` Patellaria foliacea '' var .
`` arbuscula '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1839 r. Julius von Flotow , przenosząc go do rodzaju `` Cladonia '' .
Gatunek ten występuje w dwóch podgatunkach :
`` Cladina arbuscula '' ( Wallr . )
Burgaz , subsp .
`` arbuscula '' , `` Cladonia sylvatica '' var .
`` scabrosa '' Leight .
