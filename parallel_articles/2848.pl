Chrobotek rdzawy
Chrobotek rdzawy ( `` Cladonia ochrochlora '' ( Flörke ) – gatunek grzybów należący do rodziny chrobotkowatych ( `` Cladoniaceae '' ) .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cladoniaceae , Lecanorales , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Niektóre synonimy naukowe :
Nazwa polska według `` Krytycznej listy porostów i grzybów naporostowych Polski '' .
Morfologia .
Plecha pierwotna jest trwała .
