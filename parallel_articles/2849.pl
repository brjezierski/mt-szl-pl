Cladosporium cladosporioides
Cladosporium cladosporioides ( Fresen . )
G.A .
de Vries – gatunek grzybów z klasy `` Dothideomycetes '' .
Jeden z najczęściej spotykanych grzybów mikroskopijnych , czasami chorobotwórczy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cladosporium , Cladosporiaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1850 r. Georg Fresenius nadając mu nazwę `` Penicillium cladosporioides '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Gerardus Albertus de Vries w 1952 r .
Synonimy :
Siedlisko i warunki rozwoju .
Przedstawiciele rodzaju `` Cladosporium '' należą do najczęściej spotykanych grzybów mikroskopijnych na świecie , wśród nich zaś najczęstszymi są `` C. cladosporioides '' i `` C. herbarum '' , zarówno w środowisku zewnętrznym , jak i wewnętrznym .
