Cladosporium cucumerinum
Cladosporium cucumerinum Ellis & amp ; Arthur – gatunek grzybów z klasy `` Dothideomycetes '' .
U roślin z rodziny dyniowatych ( `` Cucurbitaceae '' ) powoduje chorobę o nazwie parch dyniowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cladosporium , Cladosporiaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1889 r. Job Bicknell Ellis i Joseph Charles Arthur na ogórku w stanie Nowy York w USA .
Synonimy :
Morfologia i rozwój .
Na naturalnym podłożu i na podłożach hodowlanych tworzy blado szarooliwkowe , aksamitne kolonie .
Rewers na agarze słodowym zielonkawoczarny .
Strzępki czasami spiralnie skręcone , strzępki zanurzone zazwyczaj ze śluzowatym płaszczem .
Konidiofory wielkoniciowe , o długości do 400 µm i szerokośći 3-5 µm , czasem nabrzmiałe do 8 µm szerokości u podstawy , drobnoziarniste , jasnooliwkowobrązowe , gładkie .
Konidia z 0-2 przegrodami , o długości do 30 µm , średnicy 3-5 µm , gładkie .
Zlepione są w długie , rozgałęzione łańcuchy , 0 ( -1 ) -przegrodowe , cylindryczne , zaokrąglone na końcach , elipsoidalne do wrzecionowatych lub podkuliste , gładkie do delikatnie brodawkowatych , jasnooliwkowobrązowe , 4-9 ( -25 ) x ( 2- ) 3-5 ( -6 ) µm .
Rozprzestrzenia się przez konidia przenoszone wiatrem .
Przeżywa w glebie na resztkach roślin żywicielskich .
Występowanie .
`` Cladosporium cucumerinum '' jest rozpowszechniony w Ameryce Północnej i Europie , także w części Afryki , Azji Południowo-Wschodniej , Panamy i Trynidadu .
Choroba nie została odnotowana w Australazji ani w Ameryce Południowej .
Żywiciele : ogórek `` Cucumis sativus '' , melon `` Cucumis melo '' , dynia `` Cucurbita pepo '' .
