Cladosporium herbarum ( Pers . )
Link – gatunek grzybów z klasy `` Dothideomycetes '' .
Bardzo pospolity grzyb mikroskopijny , saprotrof lub słaby pasożyt rozwijający się głównie na powierzchni martwych lub żywych części roślin .
W laboratoriach często powoduje zanieczyszczenie kultur .
U zbóż wywołuje czerń zbóż , dla ludzi jest jednym z ważniejszych alergenów .
Powoduje zapalenie rogówki i alergiczną grzybicę płuc .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cladosporium , Cladosporiaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1794 r. Christiaan Hendrik Persoon nadając mu nazwę `` Dematium herbarum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Johann Heinrich Friedrich Link w 1816 r .
Ma około 50 synonimów .
