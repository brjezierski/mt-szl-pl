powodujący alergie i inne sporadyczne choroby u ludzi .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cladosporiaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1882 r. Albert Julius Otto Penzig
Siedlisko .
`` Cladosporium sphaerospermum '' to rozprzestrzeniony na całym świecie saprotroficzny występujący w różnorodnych siedliskach .
Przeważnie rozprzestrzenia się drogą powietrzną , jego zarodniki znajduje się w powietrzu zarówno na zewnątrz , jak i wewnątrz budynków. , także na przedmiotach i na ciele ludzi .
Jest halofitem tolerującym silne zasolenie , ale może rozmnażać się także na siedliskach o średnim i małym zasoleniu .
Powszechnie występuje na sufitach pryszniców i łazienek w postaci czarnych plam .
Może żywić się olejem zawartym w farbach i jest bardzo trudny do usunięcia .
