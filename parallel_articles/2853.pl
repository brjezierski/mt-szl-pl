Clamart
Clamart – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Według danych na rok 2012 gminę zamieszkiwało 52 408 osób , a gęstość zaludnienia wynosiła 5 976 osób/km² .
Wśród 1287 gmin regionu Île-de-France Clamart plasuje się na 437. miejscu pod względem powierzchni .
W części Clamart zwanej Petit-Clamart doszło 22 sierpnia 1962 do nieudanego zamachu na prezydenta Francji Charles ’ a de Gaulle ’ a , zorganizowanego przez Jeana-Marie Bastien-Thiry ’ ego .
W pobliżu Clamart w 1961 r. doszło do tąpnięcia na obszarze 6 hektarów w rejonie nieczynnych kamieniołomów kredy .
W jego wyniku zginęło 21 osób .
