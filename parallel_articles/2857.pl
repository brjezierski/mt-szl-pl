Okratek czerwony
Okratek czerwony ( `` Clathrus ruber '' Mich. ex Pers . )
– gatunek grzybów z rodziny sromotnikowatych ( `` Phallaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Clathrus , Phallaceae , Phallales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podał F. Teodorowicz w 1939 r .
Morfologia .
Kulisty , otwarty , mający postać czerwonej kraty z dużymi oczkami .
Wydziela bardzo nieprzyjemny zapach zwabiający owady .
Gładkie , owalno-eliptyczne , o wymiarach 4–6 × 1,5–2,5 μm .
Występowanie .
Przypuszczalnie pierwotnie występował w obszarze basenu Morza Śródziemnego i południowej Francji .
Został zawleczony do wielu krajów .
W Wielkiej Brytanii znajdowany od połowy XIX wieku .
W Polsce pierwszy okaz znaleziono na Cmentarzu Centralnym w Szczecinie w latach 80 .
Występuje w Stanach Zjednoczonych , skąd pochodzi podobny gatunek `` Clathrus crispus '' .
Znany jest z Australazji , Wysp Kanaryjskich , Karaibów , Azorów , Kanady , Meksyku , Japonii .
Na Ukrainie ma status gatunku chronionego i umieszczony jest w Czerwonej Księdze .
Filatelistyka .
Poczta Polska wyemitowała 30 czerwca 1980 r. znaczek pocztowy przedstawiający okratka czerwonego o nominale 2 złote .
Autorem projektu znaczka był prof. Alojzy Balcerzak .
Znaczek pozostawał w obiegu do 31 grudnia 1994 r ..
