Claude Lévi-Strauss
Claude Lévi-Strauss ( ur .
28 listopada 1908 w Brukseli , zm .
30 października 2009 w Paryżu ) – francuski antropolog .
Twórca strukturalizmu w antropologii kulturowej .
Lata wędrówki i nauki : Francja , Brazylia , Stany Zjednoczone .
Rodzina Lévi-Straussa opuściła Belgię podczas I wojny światowej i przeniosła się do Francji .
Claude dorastał w XVI dzielnicy Paryża , mieszkał przy ulicy Nicolasa Poussina .
Uczył się w ekskluzywnym Liceum Hoche w Wersalu , potem w Janson-de-Sailly w Paryżu .
Studiował prawo i filozofię na Sorbonie .
Nie ukończył studiów prawniczych ; w 1931 roku otrzymał agregację z filozofii .
Po kilku latach nauczania filozofii w gimnazjach w Mont-de-Mersan i Laon , namówiony przez Celestyna Bougle , dyrektora École normale supérieure , postanowił w 1935 roku wziąć udział we francuskiej misji kulturalnej w Brazylii .
