Goździeniowiec wrzecionowaty
Goździeniowiec wrzecionowaty ( `` Clavulinopsis fusiformis '' ( Sowerby ) Corner ) – gatunek grzybów z rodziny goździeńcowatych ( `` Clavariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Clavulinopsis , Clavariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1799 r. James Sowerby nadając mu nazwę `` Clavaria fusiformis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1950 r. Edred John Henry Corner , przenosząc go do rodzaju `` Clavulinopsis '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Tworzy gęste kępki nierozgałęzionych pałeczek o wysokości 5-15 cm .
Każda z pałeczek ma średnicę do 1,5 cm i wyrasta z delikatnie filcowatej , białej podstawy .
Pałeczki na przekroju poprzecznym są cylindryczne , ale dość często spłaszczone , a czasami podłużnie rowkowane .
