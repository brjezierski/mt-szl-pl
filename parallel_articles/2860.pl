Morfologia .
W kształcie prostej lub zgiętej pałeczki o wysokości 1,5–6 cm i grubości 1–5 mm , czasami zwężający się ku podstawie .
Wierzchołek jest tępy lub ostro zakończony .
Powierzchnia sucha , gładka , jaskrawa , barwy cytrynowożółtej , złotożółtej , pomarańczowożółtej .
