Goździeniowiec piękny
Goździeniowiec piękny ( `` Clavulinopsis laeticolor '' ( Berk .
& amp ; M.A .
Curtis ) R.H. Petersen ) – gatunek grzybów z rodziny goździeńcowatych ( `` Clavariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Clavulinopsis , Clavariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1868 r. Miles Joseph Berkeley i Moses Ashley Curtis nadając mu nazwę `` Clavaria laeticolor '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1965 r. R.H. Petersen , przenosząc go do rodzaju `` Clavulinopsis '' .
Synonimów nazwy naukowej ma ponad 20 .
