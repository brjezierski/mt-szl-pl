Clermont-Ferrand
Clermont-Ferrand ( oksyt .
`` Clarmont-Ferrand '' , `` Clarmont d'Auvèrnhe '' ) – miasto i gmina w środkowej Francji , w regionie Owernia-Rodan-Alpy , prefektura departamentu Puy-de-Dôme i siedziba okręgu Clermont-Ferrand .
Do 31 grudnia 2015 stolica byłego regionu Owernia .
Według danych na 1 stycznia 2018 miasto zajmowało powierzchnię 42,67 km² , a jego populacja wynosiła 146 734 mieszkańców ( 24. miejsce we Francji ) .
Miasto stanowi centrum aglomeracji Clermont Auvergne Métropole , którą w 2018 r. na 300,60 km² strefy zurbanizowanej ( `` communauté urbaine '' ) zamieszkiwało 294 127 osób .
Przestrzeń aglomeracji ( `` aire urbaine '' ) o powierzchni 2415 km² zamieszkuje 485 315 osób , co plasuje ją na 19. miejscu tej statystyki .
Miasto położone jest na wysokości 321–602 m n.p.m. , u stóp pasma wygasłych wulkanów Masywu Centralnego , m.in .
Puy de Dôme .
Historia .
Nazwa miasta powstała przez połączenie nazw dwóch miast , które zostały połączone w roku 1630 : Clermont i Montferrand .
Clermont jest jednym z najstarszych miast kraju .
