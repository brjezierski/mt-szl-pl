Lejkówka szarobiaława
Lejkówka szarobiaława ( `` Clitocybe robusta '' Peck ) – gatunek grzybów z rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Clitocybe , Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1897 r. Charles Horton Peck .
Synonim : `` Lepista robusta '' ( Peck ) Harmaja 1976 .
Władysław Wojewoda w 2003 r. podał polską nazwę lejkówka biaława , ale w spisie treści ten sam gatunek ma nazwę lejkówka szarobiaława .
Nazwa lejkówka biaława jest błędna , taką samą nazwę bowiem W. Wojewoda podaje dla innego gatunku – `` Clitocybe candicans '' .
