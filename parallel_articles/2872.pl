Lejkówka pępkowata
Lejkówka pępkowata ( `` Clitocybe umbilicata '' P .
Kumm . )
– gatunek grzybów z rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Clitocybe , Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1871 r. Paul Kummer .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica do 6 cm , początkowo wypukły , ale szybko staje się lejkowaty , z silnie wywiniętym do góry brzegiem .
Powierzchnia w stanie wilgotnym gładka , brązowa , w stanie suchym jasnoszarobrązowa .
