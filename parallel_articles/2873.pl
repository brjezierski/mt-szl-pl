Lejkóweczka postrzępiona
Lejkóweczka postrzępiona ( `` Clitocybula lacerata '' ( Scop . )
Métrod ) – gatunek grzybów z rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Clitocybe , Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1772 r. Joannes Antonius Scopoli nadając mu nazwę `` Agaricus laceratus '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Georges Métrod w 1952 r .
