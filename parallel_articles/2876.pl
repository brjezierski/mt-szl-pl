Cluny słynne jest z istniejącego tu niegdyś opactwa benedyktynów , założonego w 910 roku , z kościołem będącym jedną z największych świątyń chrześcijańskich swego czasu .
Klasztor został zamknięty w czasie rewolucji francuskiej i prawie całkowicie rozebrany w 1811 roku .
Dziś miasto jest ważnym ośrodkiem turystycznym .
