Cobán
Cobán ( kekczi : `` Kob'an '' ) – miasto w środkowej Gwatemali położone nad rzeką Cahabón , ośrodek administracyjny departamentu Alta Verapaz .
W 2012 miasto liczyło blisko 69 tys .
mieszkańców a cała gmina ponad 96 tys .
Przetwórstwo kawy ; przemysł skórzany , włókienniczy , drzewny .
W pobliżu miasta znajduje się port lotniczy Coban .
Na terenie gminy leży park narodowy ( hiszp .
`` Parque Nacional Las Victorias '' ) , będący najczęściej odwiedzanym miejscem turystycznym na terenie departamentu Alta Verapaz .
Lokalny stadion Estadio Verapaz za sprawą nietypowego położenia ( dookoła stadionu roztaczają się las sosnowy oraz wysokie wzniesienia , z których można oglądać spotkanie ) jest uznawany za jeden z najbardziej nietypowych stadionów w Ameryce .
Swoje mecze rozgrywa na nim klub piłkarski Cobán Imperial .
