Coca-Cola
Coca-Cola ( w USA , Kanadzie , Australii i Wielkiej Brytanii znana jako Coke ) – marka napoju gazowanego przedsiębiorstwa The Coca-Cola Company .
Powstała pod koniec XIX wieku i jest jedną z najpopularniejszych i najbardziej rozpoznawalnych marek na świecie .
Od 1960 roku kształt butelki jest prawnie zastrzeżony .
Coca-cola jest jedną z ikon kultury masowej XX wieku , jak również jednym z symboli Stanów Zjednoczonych .
Historia .
Receptura coca-coli została opracowana w 1886 roku przez J.S .
Pembertona , farmaceutę z Atlanty .
Był to jego drugi napój na bazie liści koki i orzechów koli ( pierwszy nosił nazwę Pemberton ’ s French Wine Coca ) .
Początkowo sprzedaż coca-coli nie była rentowna , jednak po przejęciu jej przez Asę Candlera ( który w 1893 roku zarejestrował oficjalnie markę ) i jego sprawnym zabiegom marketingowym napój stał się hitem , a zyski ze sprzedaży osiągnęły 50 mln USD w 1912 roku .
Po śmierci Candlera w 1919 przedsiębiorstwo przeszło w ręce bankiera , Ernesta Woodruffa , a później w ręce jego syna Roberta Woodruffa , który kierował spółką do śmierci w 1985 roku .
W 1906 roku rozpoczęła się ekspansja przedsiębiorstwa poza Stany Zjednoczone .
Coca-cola trafiła m.in .
na Kubę i do Panamy .
Coca-cola pojawiła się w butelkach w 1894 roku za sprawą dwóch przedsiębiorców , którzy otrzymali od Asy Candlera prawa do rozlewania napoju za cenę symbolicznego jednego dolara .
