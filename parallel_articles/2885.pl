Coccomyces dentatus
Coccomyces dentatus ( J.C. Schmidt & amp ; Kunze ) Sacc .
– gatunek grzybów z rodziny łuszczeńcowatych ( `` Rhytismataceae '' ) .
Rozwija się na opadłych liściach drzew .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Coccomyces , Rhytismataceae , Rhytismatales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1816 r. Johann Carl Schmidt i Gustav Kunze nadając mu nazwę `` Phacidium dentatum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1877 r. Pier Andrea Saccardo .
Ma 12 synonimów .
Niektóre z nich :
Morfologia .
Jej owocniki pojawiają się śródnaskórkowo , wcześniej niż apotecja .
