Cochabamba ( departament )
Cochabamba ( czyt .
koczabamba ) – departament w środkowej Boliwii .
Zajmuje powierzchnię 55 631 km² .
W 2012 roku liczył 1 762 761 mieszkańców , a gęstość zaludnienia wynosiła 31,7 mieszk./km² .
Stolicą departamentu jest Cochabamba .
Dzieli się na 16 prowincji .
Demografia .
W 2012 roku populacja departamentu liczyła 1 762 761 mieszkańców .
W porównaniu z 2001 rokiem rosła ona średnio o 1,69 % rocznie .
