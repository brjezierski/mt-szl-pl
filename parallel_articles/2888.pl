Bipolaris zeicola
Bipolaris zeicola ( G.L .
Stout ) Shoemaker – gatunek grzybów z klasy `` Dothideomycetes '' .
Mikroskopijny grzyb pasożytniczy , będący jednym z patogenów wywołujących chorobę o nazwie helmintosporioza liści kukurydzy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Bipolaris '' , `` Pleosporaceae '' , `` Pleosporales '' , `` Pleosporomycetidae '' , `` Dothideomycetes '' , `` Pezizomycotina '' , `` Ascomycota '' , `` Fungi '' .
Synonimy :
Aktualna nazwa naukowa pochodzi od anamorfy .
Jeszcze do niedawna teleomorfę opisywano jako odrębny gatunek – `` Cochliobolus carbonum '' .
Morfologia .
Konidiofory brunatne , nierozgałęzione , proste , pojedynczo wyrastające z grzybni pasożytującej w porażonej roślinie .
Komórki konidiogenne z kilkoma punktami tworzenia konidiów .
Konidia powstałe w koloniach hodowanych na PDA morfologicznie różnią się od konidiów tworzonych na liściach kukurydzy porażonej przez tego patogena ; ich średnia długość ( 31,9 μm ) jest o połowę mniejsza niż na naturalnie porażonych liściach ( 65,2 μm ) .
Komórki konidiogenne zlokalizowane terminalnie i subterminalnie na krótkich konidioforach wytwarzają nowe konidia , które zachowują się jak konidia inicjalne , czyli natychmiast wydłużają się lub kiełkują .
Czasami pojawiają się w łańcuchach .
Są brunatne , poprzedzielane poprzecznie kilkoma distoseptami .
Pierwsza septa tworzy się w środku konidium , lub w jego pobliżu , druga oddziela komórkę przy podstawie , trzecia przy wierzchołku .
Kiełkują jedną lub oboma komórkami biegunowymi .
