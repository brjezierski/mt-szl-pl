Cochliobolus heterostrophus
Cochliobolus heterostrophus ( Drechsler ) Drechsler – gatunek grzybów z klasy `` Dothideomycetes '' .
Jest jednym z patogenów u kukurydzy wywołujących chorobę o nazwie helmintosporioza liści kukurydzy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cochliobolus , Pleosporaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1925 r. Charles Frank Drechsler nadając mu nazwę `` Ophiobolus heterostrophus '' .
Ten sam autor w 1934 r. przeniósł go do rodzaju `` Cochliobolus '' .
Synonimy :
Anamorfy `` Bipolaris maydis '' i `` Drechslera maydis '' dawniej opisywane były jako odrębne gatunki .
Obecnie to synonimy .
Morfologia .
Grzyb mikroskopijny .
Pseudotecja kuliste , elipsoidalne lub w kształcie kolby , ciemnobrązowe do czarnych , wolne lub rozwijające się na kolumnowej lub płaskiej podkładce .
Mają dobrze odgraniczonym dziobek ostioli lub cylindryczną szyjkę , często pokrytą brązowymi , sterylnymi strzępkami w górnej połowie pseudotecjum , lub z kilkoma komórkami szklistymi na wierzchołku dzioba .
Ściana pseudotecjum zbudowana jest z komórek pseudoparenchymatycznych , mniej więcej tej samej grubości , lub nieco bardziej pogrubionych w górnej części .
Worki liczne , cienkościenne , cylindryczne do szeroko wrzecionowatych , 1-8 , przeważnie 8 zarodnikowe , wyrastające ze szklistych cienkościennych komórek hymenium .
Askospory nitkowate , szkliste , ale mogą stać się bladobrązowe .
Są wieloprzegrodowe , czasami zwężone w przegrodzie , czasami z cienką , szklistą osłonką śluzowatą , silnie helikoidalną lub luźno zwiniętą w worku .
Nibywstawki szkliste , nitkowate , septowane i rozgałęzione .
