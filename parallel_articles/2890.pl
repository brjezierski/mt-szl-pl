Cody Gakpo
Cody Gakpo ( ur .
7 maja 1999 w Eindhoven ) – holenderski piłkarz pochodzenia togijskiego występujący na pozycji napastnika w angielskim klubie Liverpool FC oraz w reprezentacji Holandii .
Kariera klubowa .
W czasach juniorskich trenował w EVV Eindhoven AV ( do 2007 roku ) i PSV Eindhoven ( 2007–2018 ) .
W 2018 roku dołączył do drugiego zespołu PSV , dwa lata później trafił natomiast do pierwszej drużyny .
W rozgrywkach Eredivisie zadebiutował 25 lutego 2018 w wygranym 3:1 meczu z Feyenoordem .
Do gry wszedł w 92. minucie , zastępując Stevena Bergwijna .
W sezonie 2017/2018 wraz z klubem zdobył mistrzostwo kraju .
28 grudnia 2022 ogłoszono porozumienie między PSV Eindhoven , a Liverpoolem w kwestii transferu Gakpo .
7 stycznia 2023 w meczu Pucharu Anglii z Wolverhampton zadebiutował w klubie .
13 lutego w meczu Premier League przeciwko Everton ( 2:0 ) strzelił premierową bramkę dla angielskiego klubu .
Kariera reprezentacyjna .
W reprezentacji Holandii zadebiutował 21 czerwca 2021 w wygranym 3:0 meczu Mistrzostw Europy 2020 z Macedonią .
Pierwszą bramkę strzelił 4 września w meczu eliminacji do Mistrzostw Świata 2022 przeciwko reprezentacji Czarnogóry ( 4:0 ) .
