Colchester
Colchester ( IPA ˈkəʊltʃɛstə ) – miasto o statusie city w Wielkiej Brytanii , w Anglii , w hrabstwie Essex , w dystrykcie Colchester .
Uznane za najstarsze miasto w Wielkiej Brytanii , założone przez Rzymian ( wymienione przez Pliniusza Starszego w roku 77 ) .
Przez miasto przepływa rzeka Colne .
Colchester jest siedzibą Uniwersytetu Essex .
Toponimia .
Celtycka nazwa miasta to Camulodunon ( od celtyckiego boga wojny ) .
Od roku 49 , gdy Rzymianie posunęli się na północ , miasto znane było pod nazwą Colonia Claudia Victricensis .
Ten oficjalny tytuł sugeruje , że weterani , którzy mieli się tu osiedlić , pochodzili głównie z legionu XX Valeria Victrix ; ale bardziej prawdopodobne jest , że tytuł miał upamiętnić zwycięstwo Klaudiusza nad Brytanią ( tytuł `` victrix '' mógł nie zostać przyznany legionowi , jak to było w przypadku czternastego legionu , aż do momentu , gdy okazało się , jak ogromna rolę odegrał on w tłumieniu powstania Boudiki ( Colchester ) , na rzece Colne ) .
Colchester jest wspomniana w Domesday Book ( 1086 ) jako `` Colecastro '' / '' Colecestra '' .
