Coleosporium tussilaginis
Coleosporium tussilaginis ( Pers . )
Lév . )
– gatunek grzybów z rodziny pęcherzycowatych ( `` Coleosporiaceae '' ) .
Pasożyt atakujący wiele gatunków roślin .
U sosny wywołuje chorobę zwaną rdzą pęcherzykowatą igieł sosny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Coleosporiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo tussilaginis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1849 r. Joseph Henri Léveillé .
Ma około 60 synonimów .
Rozwój .
Jest to pasożyt dwudomowy , tzn , że jego pełny cykl rozwojowy odbywa się na dwóch różnych gatunkach żywicieli .
Jednym z nich są niektóre gatunki sosen , drugim wiele gatunków roślin zielnych .
Wiosną ( od kwietnia do maja ) na zakażonych igłach sosny pojawiają się niewielkie plamki .
