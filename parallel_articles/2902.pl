Coleroa alchemillae ( Grev . )
G. Winter – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju przywrotnik ( `` Alchemilla '' ) .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Coleroa , Venturiaceae , Venturiales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1824 r. Robert Kaye Greville nadając mu nazwę `` Asteroma alchemillae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Heinrich Georg Winter w 1885 r .
