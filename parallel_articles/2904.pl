Colima ( wulkan )
Colima – kompleks składający się z dwóch szczytów wulkanicznych w Kordylierze Wulkanicznej w zachodnim Meksyku .
Wyższy i starszy z nich `` Nevado de Colima '' ma wysokość 4330 m n.p.m .
i jest nieczynny .
Młodszy ze szczytów `` Volcán de Colima '' ma wysokość 3860 m n.p.m .
i jest obecnie bardzo aktywnym wulkanem .
Ostatnią znaczącą erupcję odnotowano 18 stycznia 2017 .
