Colletotrichum acutatum
Colletotrichum acutatum J.H .
Simmonds – gatunek grzybów z rodziny `` Glomerellaceae '' .
Mikroskopijny grzyb pasożytniczy u roślin wywołujący choroby zwane antraknozami .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Glomerellaceae , Glomerellales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1968 r. J.H .
Simmonds na melonowcu właściwym ( `` Carica papaya '' ) w Queensland .
Synonimy :
Uznana nazwa naukowa pochodzi od anamorfy .
Teleomorfa opisywana została jako `` Glomerella acutata '' .
Charakterystyka .
Znana jest głównie anamorfa .
Od innych gatunków `` Colletotrichum '' odróżnia się głównie wąskimi ( o szerokości poniżej 5 μm ) i ostro zakończonymi konidiami .
Jednakże konidia te są zróżnicowane pod względem kształtu i wielkości , co utrudnia identyfikację gatunku , szczególnie trudno go odróżnić od `` Glomerella cingulata '' .
W 2001 r. udało się zaobserwować teleomorfę w hodowli `` in vitro '' .
W warunkach naturalnych na porażonych miejscach , w środkowej części nekroz grzybnia tworzy zlepioną śluzem masę konidiów o barwie od łososiowej do brązowej .
Następuje to w czasie wilgotnej pogody .
