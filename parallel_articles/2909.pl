Colletotrichum coccodes
Colletotrichum coccodes ( Wallr . )
S. Hughes – gatunek grzybów z rodziny `` Glomerellaceae '' .
Wśród roślin uprawnych w Polsce wywołuje choroby o nazwie antraknoza pomidora i antraknoza ziemniaka .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Colletotrichum , Glomerellaceae , Glomerellales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1833 r. Karl Friedrich Wallroth nadając mu nazwę `` Chaetomium coccodes '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Stanley John Hughes w 1958 r .
Synonimów ma ponad 50 .
Gatunek ten znany jest wyłącznie jako anamorfa , jak dotąd nie zaobserwowano jego teleomorfy .
Na podstawie badań molekularnych i ultrastrukturalnych uważa się , że jest nią któryś z gatunków w obrębie rodzaju `` Glomerella '' .
