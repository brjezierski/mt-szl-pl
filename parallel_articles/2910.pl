Colletotrichum lindemuthianum
Colletotrichum lindemuthianum ( Sacc .
& amp ; Magnus ) Briosi & amp ; Cavara – gatunek grzybów z rodziny `` Glomerellaceae '' .
Wywołuje chorobę o nazwie antraknoza fasoli
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Colletotrichum , Glomerellaceae , Glomerellales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1878 r. Pier Andrea Saccardo i Paul Wilhelm Magnus nadając mu nazwę `` Gloeosporium lindemuthianum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu G. Briosi i Fridiano Cavara w 1889 r .
Synonimy
Gatunek ten znany jest wyłącznie jako anamorfa , jak dotąd nie zaobserwowano jego teleomorfy .
Na podstawie badań molekularnych i ultrastrukturalnych uważa się , że jest nią któryś z gatunków w obrębie rodzaju `` Glomerella '' .
Morfologia i rozwój .
Acerwulusy pojawiają się na łodydze , liściach i owocach w grupach , lub pojedynczo , rozrzucone .
Są zaokrąglone lub jajowate , o średnicy do 300 μm .
Powstają wewnątrz tkanek i pod naskórkiem żywiciela , powodując zaburzenia jego tkanek .
