Pieniążek drobniutki ( `` Collybia cirrhata '' ( Schumach . )
Quél . )
– gatunek grzybów z rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Collybia , Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1803 r. Christian Friedrich Schumacher nadając mu nazwę `` Agaricus cirrhatus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1872 r. Lucien Quélet .
