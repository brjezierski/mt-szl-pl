Colmar-Berg
Colmar-Berg − gmina i miasteczko w centralnym Luksemburgu , w dystrykcie Luksemburg , w kantonie Mersch .
W 2005 roku , miasto Colmar-Berg , które znajduje się we wschodniej części gminy , zamieszkiwało 1711 osób .
Tutaj znajduje się rezydencja wielkiego księcia Luksemburga , Zamek Berg .
