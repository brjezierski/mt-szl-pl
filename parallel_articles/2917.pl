Colorado Springs
Colorado Springs – miasto w Stanach Zjednoczonych , położone w środkowo-zachodniej części kraju , w stanie Kolorado .
Znajduje się na styku Wielkich Równin i Gór Skalistych ( Front Range ) , u podnóża Pikes Peak , który wznosi się na wysokość 4302 m n.p.m. , na południe od Denver .
Siedziba i największe miasto hrabstwa El Paso .
Jest to drugie po Denver pod względem wielkości miasto w tym stanie , zamieszkane przez 479 tys .
