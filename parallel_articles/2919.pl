Columbia Pictures
Columbia Pictures Industries , Inc. – amerykańska wytwórnia filmowa założona w 1918 roku przez Jacka i Harry'ego Cohnów oraz Joego Brandta jako `` CBC Film Sales Corporation '' .
Siedziba wytwórni znajduje się w Culver City w stanie Kalifornia .
Razem z TriStar Pictures i innymi wytwórniami należy do Columbia TriStar Motion Picture Group , holdingu Sony Pictures Entertainment .
W latach 80 .
XX wieku była bardzo znaną i największą wytwórnią filmową w Stanach Zjednoczonych .
Wytwórnia posiada jednostkę zależną Ghost Corps , zajmującą się głównie produkcją filmów z serii `` Pogromcy duchów '' .
Historia .
Przedsiębiorstwo zostało założone w 1918 roku jako `` CBC Film Sales Corporation '' .
