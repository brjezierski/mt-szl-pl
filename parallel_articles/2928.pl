Comino
Comino ( malt .
Kemmuna ) trzecia co do wielkości i najmniejsza zamieszkana wyspa archipelagu Wysp Maltańskich , położona pomiędzy Maltą a Gozo .
Na wyspie znajduje się jedna z głównych atrakcji turystycznych Malty – Blue Lagoon ( Błękitna Laguna ) .
Z powodu prądów morskich woda w okolicach wyspy Comino jest trochę chłodniejsza niż na plażach Malty .
Na wyspę najłatwiej można się dostać z portów w miastach Ċirkewwa ( Malta ) i Mġarr ( Gozo ) .
Istnieje możliwość skorzystania również z promów turystycznych dostępnych w większości miejsc turystycznych .
Na wyspie istnieje wyznaczone miejsce do lądowania śmigłowców – heliport ( współrzędne geograficzne : ) .
Dzięki wrakom będących obiektami podwodnej turystyki jest popularna wśród nurków .
