Commerzbank-Arena
Commerzbank-Arena ( wcześniej `` Waldstadion '' ) – stadion o pojemności 52 300 , który znajduje się we Frankfurcie nad Menem .
Używany do rozgrywania spotkań piłkarskich oraz amerykańskiego futbolu .
Jest to stadion drużyny Eintracht Frankfurt .
Historia .
Waldstadion otwarty został w 1925 r. Podczas II wojny światowej wykorzystywany był również do różnych wystąpień politycznych .
Rozgrywano na nim mecze Mistrzostw Świata w Piłce Nożnej w 1974 i Mistrzostw Europy w Piłce Nożnej w 1988 oraz mecz otwarcia i finał Mistrzostw Europy w Futbolu Amerykańskim 2010 .
W latach 2008–2010 na stadionie rozgrywany był finał niemieckiej ligi futbolu amerykańskiego , German Bowl .
W lipcu 2005 otrzymał nazwę `` „ Commerzbank-Arena ” '' .
Podczas Mistrzostw Świata 2006 w Niemczech , stadion był nazywany `` „ FIFA WM Stadion Frankfurt ” '' .
Przed mistrzostwami został przebudowany i jest obecnie jednym z największych stadionów Niemiec .
