Compsomyces lestevae
Compsomyces lestevae Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Compsomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1900 r. Roland Thaxter na owadzie `` Lesteva sicula '' w Anglii .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski opisał jego występowanie na chrząszczu `` Lesteva pubescens '' z rodziny kusakowatych ( Staphylinidae ) .
