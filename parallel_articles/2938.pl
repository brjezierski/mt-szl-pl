Conegliano ( stacja kolejowa )
Conegliano ( włoski : `` Stazione di Conegliano '' ) – stacja kolejowa w Conegliano , w prowincji Treviso , w regionie Wenecja Euganejska , we Włoszech .
Znajduje się na linii Wenecja-Udine i Conegliano-Ponte Alpi .
Historia .
Stacja została otwarta w dniu 1 maja 1855 przy okazji otwarcia linii Treviso Centrale - Pordenone .
Ruch .
Stacja obsługuje wszystkie pociągi regionalne na linii kolejowej Wenecja - Udine i Calalzo – Conegliano .
