Stożkówka korzeniasta
Stożkówka korzeniasta ( `` Conocybe antipus '' ( Lasch ) Kühner ) – gatunek grzybów z rodziny gnojankowatych ( `` Bolbitiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Conocybe , Bolbitiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1828 r. Wilhelm Gottlob Lasch nadając mu nazwę `` Agaricus antipus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Robert Kühner w 1935 r .
Ma 12 synonimów .
Niektóre z nich :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 1–3,5 cm , kształt początkowo wypukły , potem płaskowypukły , na koniec płasko rozpostarty .
Brzeg regularny , cienki , lekko podwinięty .
