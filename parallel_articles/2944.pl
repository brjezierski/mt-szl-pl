Stożkówka złota
Stożkówka złota ( `` Conocybe aurea '' ( Jul .
Schäff . )
Hongo ) – gatunek grzybów z rodziny gnojankowatych ( `` Bolbitiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Conocybe , Bolbitiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1930 r. Julius Schäffer nadając mu nazwę `` Galera aurea '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Tsuguo Hongo w 1963 roku
Synonimy :
W checklist W. Wojewody w 2003 r. brak tego gatunku .
Jego polską nazwę podaje internetowy atlas grzybów .
Morfologia .
