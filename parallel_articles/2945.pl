Stożkówka czerwcowa
Stożkówka czerwcowa ( `` Conocybe juniana '' ( Velen . )
Hauskn .
& amp ; Svrček ) – gatunek grzybów z rodziny gnojankowatych ( `` Bolbitiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Conocybe , Bolbitiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1947 r. Josef Velenovský nadając mu nazwę `` Galera juniana '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Anton Hausknecht i Mirko Svrček w 1999 r .
