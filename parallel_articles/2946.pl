Conocybe rugosa
Conocybe rugosa ( Peck ) Watling – gatunek grzybów z rodziny gnojankowatych ( `` Bolbitiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Conocybe , Bolbitiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1898 r. Charles Horton Peck nadając mu nazwę `` Pholiota rugosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Roy Watling w 1981 r .
Synonimy :
Morfologia .
Średnica 1–2 cm , początkowo szeroko stożkowy do szeroko dzwonkowatego , później przechodzący w szeroko wypukły do płaskowypukłego , czasem z centralnym garbkiem .
Powierzchnia w dotyku tłusta , gładka , naga lub drobno pomarszczona na środku , ciemnopomarańczowo-brązowa na środku i żółtawo-brązowa w kierunku brzegu .
Brzeg prążkowany , mniej więcej do połowy kapelusza .
Wąsko przyrośnięte , gęste , z licznymi międzyblaszkami , białawe , przechodzące w brązowawe , początkowo pokryte białawą częściową osłoną .
