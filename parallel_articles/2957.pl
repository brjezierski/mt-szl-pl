Czernidłak kępkowy
Czernidłak kępkowy ( `` Tulosesus congregatus '' ( Bull . )
D. Wächt .
& amp ; A. Melzer ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Tulosesus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1786 r. Jean Baptiste François Bulliard jako `` Agaricus congregatus '' .
W 1838 r. Elias Fries przeniósł go do rodzaju `` Coprinus '' ( czernidłak ) , a w 1879 r. Petter Karsten do rodzaju `` Coprinellus '' .
Obecną nazwę nadali mu D. Wächt .
