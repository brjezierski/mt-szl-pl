Coprinellus ellisii
Coprinellus ellisii ( P.D .
Orton ) Redhead , Vilgalys & amp ; Moncalvo – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinellus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1960 r. Peter Darbishire Orton nadając mu nazwę `` Coprinus ellisii '' .
W 2001 r. Redhead , Vilgalys i Moncalvo w wyniku badań filogenetycznych stwierdzili , że rodzaj `` Coprinus '' jest polifiletyczny i rozbili go na kilka rodzajów .
Występowanie .
W Europie `` Coprinopsis ellisii '' to gatunek szeroko rozprzestrzeniony , ale rzadki .
W Polsce do 2011 r. był nieznany .
Brak go w Krytycznej liście wielkoowocnikowych grzybów podstawkowych Polski W. Wojewody , nie wymienia go także A. Bujakiewicz w swoich listach grzybów wielkoowocnikowych Polski z roku 2004 i 2006 .
Po raz pierwszy znaleźli go 27 czerwca 2009 r. B. Gierczyk i T. Pachlewski nad potokiem Rzeczyca po zachodniej stronie zabudowań wsi Ustrzyki Górne , w pobliżu szlaku turystycznego na Wielką Rawkę .
