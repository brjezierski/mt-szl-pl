Czernidłak kłaczkowaty
Czernidłak kłaczkowaty ( `` Coprinellus flocculosus '' ( DC . )
Vilgalys , Hopple & amp ; Jacq .
Johnson ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinellus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1805 r. Augustin Pyramus de Candolle nadając mu nazwę `` Agaricus flocculosus '' .
W 1838 r. Elias Fries przeniósł go do rodzaju `` Coprinus '' .
W 2001 r. Vilgalys , Hopple i Jacq .
Johnson w wyniku badań filogenetycznych stwierdzili , że rodzaj `` Coprinus '' jest polifiletyczny i rozbili go na kilka rodzajów .
Polską nazwę zaproponował Władysław Wojewoda w 2003 r. dla synonimu `` Coprinus flocculosus '' .
Po przeniesieniu tego gatunku do rodzaju `` Coprinellus '' stała się ona niespójna z obecną nazwą naukową .
