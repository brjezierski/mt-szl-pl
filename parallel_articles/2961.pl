Coprinellus pellucidus
Coprinellus pellucidus ( P .
Karst . )
Redhead , Vilgalys & amp ; Moncalvo – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinellus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1883 r. Petter Karsten nadając mu nazwę `` Coprinus pellucidus '' .
W wyniku przeprowadzonych w pierwszej dekadzie początku XXI wieku badań filogenetycznych okazało się , że rodzaj `` Coprinus '' jest polifiletyczny i został rozbity na kilka rodzajów .
W 2001 r. Redhead , Vilgalys & amp ; Moncalvo gatunkowi temu nadali nazwę `` Coprinellus pellucidus '' .
