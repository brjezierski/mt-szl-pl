Czernidłak brodawkowany
Czernidłak brodawkowany ( `` Coprinellus radians '' ( Desm . )
Vilgalys , Hopple & amp ; Jacq .
Johnson ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinellus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1828 r. Jean Baptiste Henri Joseph Desmazières nadając mu nazwę `` Agaricus radians '' .
W 1838 r. Georges Métrod przeniósł go do rodzaju `` Coprinus '' .
W wyniku badań filogenetycznych prowadzonych na przełomie XX i XXI wieku mykolodzy ustalili , że rodzaj `` Coprinus '' jest polifiletyczny i rozbili go na kilka rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadali temu gatunkowi Vilgalys , Hopple i Jacq .
