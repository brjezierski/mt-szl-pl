Czernidłak pniowy
Czernidłak pniowy ( `` Coprinellus truncorum '' ( Bull . )
( Scop . )
Redhead , Vilgalys & amp ; Moncalvo ) – gatunek grzybów z rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinellus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten po raz pierwszy zdiagnozował w 1772 r. Giovanni Antonio Scopoli jako `` Agaricus truncorum '' Bull. , w 1838 r. Elias Fries przeniósł go do rodzaju `` Coprinus '' , a w 2001 r. Redhead , Vilgalys i Moncalvo do rodzaju `` Coprinellus '' .
Synonimy :
Nazwę polską nadał mu w 1898 r. Stanisław Chełchowski .
W 2001 r. jednak gatunek ten przeniesiono do odrębnego rodzaju `` Coprinellus '' , tak więc polska nazwa stała się niespójna z nazwą naukową .
Na niektórych polskojęzycznych stronach internetowych opisywany jest jako czernidłaczek pniowy , jest to jednak nazwa nieużywana w źródłach naukowych .
