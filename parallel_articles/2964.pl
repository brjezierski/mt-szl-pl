Czernidłak łodygowy
Czernidłak łodygowy ( `` Coprinopsis friesii '' ( Quél . )
P .
Karst . )
– gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinopsis , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1872 r. Lucien Quélet nadając mu nazwę `` Coprinus friesii '' .
W 1874 r. Elias Fries przeniósł go do rodzaju `` Coprinus '' ( czernidłak ) .
