Coprinopsis gonophylla
Coprinopsis gonophylla ( Quél . )
Redhead , Vilgalys & amp ; Moncalvo – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinopsis , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1884 r. Lucien Quélet , nadając mu nazwę `` Coprinus gonodophyllus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu S.A. Redhead , R.J. Vilgalys i J.-M. Moncalvo w 2001 r .
Morfologia .
Średnica 1–2 ( 3 ) cm , za młodu kulisty , potem cylindryczny , rzadko spiczasty , co najwyżej jajowaty .
