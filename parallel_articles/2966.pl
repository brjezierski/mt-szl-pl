Czernidłak omączony
Czernidłak omączony ( `` Coprinopsis laanii '' ( Kits van Wav . )
Redhead , Vilgalys & amp ; Moncalvo ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinopsis , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisała go w 1968 r. dr Kits van Waveren nadając mu nazwę `` Coprinus laanii '' .
W wyniku badań filogenetycznych prowadzonych na przełomie XX i XXI wieku mykolodzy ustalili , że rodzaj `` Coprinus '' jest polifiletyczny i rozbili go na kilka rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadali temu gatunkowi Redhead , Vilgalys i Moncalvo w 2001 r .
Polską nazwę zaproponował Władysław Wojewoda w 2003 r. dla synonimu `` Coprinus laanii '' .
