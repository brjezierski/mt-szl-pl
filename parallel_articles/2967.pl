Coprinopsis phlyctidospora ( Romagn . )
Redhead , Vilgalys & amp ; Moncalvo – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinopsis , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1851 r. Henri Charles Louis Romagnesi , nadając mu nazwę `` Coprinus phlyctidospora '' .
W wyniku badań filogenetycznych prowadzonych na przełomie XX i XXI wieku mykolodzy ustalili , że jest to odrębny gatunek .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Redhead , Vilgalys i Moncalvo w 2001 r .
Synonimy :
Morfologia .
Średnica 1-4 ( 5 ) cm , szary , szaroniebieski , z białymi płatkami osłony na szarym tle .
Na lodych okazach szare łuski , ale szybko odpadające .
