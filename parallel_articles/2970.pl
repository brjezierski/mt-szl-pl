Coprinopsis candidata
Coprinopsis candidata ( Uljé ) Gminder & amp ; Böhning – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinopsis , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1988 r. C.B .
Uljé , nadając mu nazwę `` Coprinus candidatus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Andreas Gminder i T. Böhning w 2016 r .
Morfologia .
Średnica 1–3 ( 5 ) cm , za młodu półkulisty , potem coraz bardziej płaski .
Powierzchnia promieniście prążkowana , beżowo-brązowa , szaro-niebieska , szaro-brązowa , przezroczysta , często z brązowawym i nie prążkowanym wierzchołkiem .
W młodych okazach jest całkowicie okryta osłoną , która później pęka na drobne płatki .
Początkowo szare , później czarnobrązowe , wolne , nie dochodzące do trzonu .
