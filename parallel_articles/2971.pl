Czernidłak pniakowy
Czernidłak pniakowy ( `` Coprinellus saccharinus '' ( Romagn . )
P. Roux , Guy García & amp ; Dumas ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Coprinellus , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1976 r. Henri Charles Louis Romagnesi , nadając mu nazwę `` Coprinus saccharinus '' .
W wyniku badań filogenetycznych prowadzonych na przełomie XX i XXI wieku mykolodzy ustalili , że rodzaj `` Coprinus '' jest polifiletyczny i rozbili go na kilka rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadali temu gatunkowi P. Roux , Guy García & amp ; Dumas w 2006 r .
Polską nazwę zaproponował Władysław Wojewoda w 2003 r. dla synonimu `` Coprinus saccharinus '' .
