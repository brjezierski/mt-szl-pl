Coprotus sexdecimsporus
Coprotus sexdecimsporus ( P. Crouan & amp ; H. Crouan ) Kimbr .
& amp ; Korf – gatunek grzybów z rzędu kustrzebkowców ( `` Pezizales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Coprotus , Incertae sedis , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1858 r. Pierre Louis Crouan i Hippolyte Marie Crouan nadając mu nazwę `` Ascobolus sexdecimsporus '' .
Obecną nazwę nadali mu James William Kimbrought i Richard Paul Korf w 1967 r .
Synonimy nazwy naukowej :
Morfologia .
Teleomorfa tworzy owocniki typu apotecjum ( miseczka ) o średnicy 0,5–1 mm , często w dużych skupiskach , ale nie zlewające się z sobą .
Początkowo są kuliste lub o kształcie zbliżonym do kulistego , a ostatecznie stają się tarczowate , przezroczyste do białych , w stanie suchym żółtawe .
Brzeg słabo zaznaczony , gładki , siedzący .
