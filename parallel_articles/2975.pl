Corazon Aquino
Maria Corazon Sumulong „ Cory ” Cojuangco Aquino ( ur .
25 stycznia 1933 w Tarlac , zm .
1 sierpnia 2009 w Makati ) – filipińska polityk , prezydent Filipin w latach 1986–1992 .
Była pierwszą kobietą-prezydentem w historii Azji .
Życiorys .
Corazon była żoną działacza opozycji , senatora Benigno Aquino , który walczył z dyktaturą Ferdinanda Marcosa .
Kiedy po powrocie z wygnania 21 sierpnia 1983 Benigno Aquino został zamordowany na lotnisku , Corazon Aquino zaangażowała się w walkę polityczną z reżimem Marcosa .
Została przywódczynią koalicji Laban , a następnie w lutym 1986 wygrała wybory prezydenckie przeciw Marcosowi .
Początkowo zarówno ona , jak i Marcos ogłosili zwycięstwo w wyborach – na skutek demonstracji społeczeństwa Marcos został jednak zmuszony do ucieczki z kraju .
W 1987 Aquino doprowadziła do wprowadzenia nowej , demokratycznej konstytucji .
Urząd prezydenta sprawowała do 30 czerwca 1992 , kiedy to zastąpił ją na tym stanowisku generał Fidel Ramos .
Corazon Aquino została wybrana Człowiekiem Roku 1986 przez magazyn „ Time ” .
