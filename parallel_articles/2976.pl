Corbin Bleu
Corbin Bleu , właściwie Corbin Bleu Reivers ( ur .
21 lutego 1989 w Nowym Jorku ) – amerykański aktor , model , tancerz i piosenkarz jamajsko-włoskiego pochodzenia .
Życiorys .
Kariera .
Już w wieku dwóch lat Bleu występował w reklamach telewizyjnych , a jako czterolatek został dziecięcym modelem agencji `` Ford Modeling Agency '' w Nowym Jorku .
Kiedy pracował jako dziecięcy model jego rodzice zauważyli , że ich pierworodny ma dryg do tańczenia .
Zapisali Corbina na zajęcia z jazzu i baletu klasycznego .
Dodatkowo chodził na lekcje śpiewu .
