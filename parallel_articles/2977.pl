Corby
Corby – miasto w Wielkiej Brytanii ( Anglia ) , w hrabstwie Northamptonshire , w dystrykcie ( `` unitary authority '' ) North Northamptonshire , około 120 km na północ od Londynu i 70 km na południowy zachód od The Wash – dużego estuarium kilku rzek uchodzącego do Morza Północnego .
W 2011 roku miasto liczyło 54 927 mieszkańców .
Kod pocztowy ( postcode ) NN17 i NN18 .
Corby jest wspomniana w Domesday Book ( 1086 ) jako `` Corb ( e ) i '' .
W mieście rozwinął się przemysł elektroniczny , metalowy oraz spożywczy .
