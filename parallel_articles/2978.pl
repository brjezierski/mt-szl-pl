Corcoran ( Kalifornia )
Corcoran – miasto ( `` city '' ) w hrabstwie Kings , w środkowej części stanu Kalifornia , w Stanach Zjednoczonych , położone w dolinie San Joaquin .
W 2013 roku miasto liczyło 23 320 mieszkańców .
Miasto założone zostało w 1914 roku .
Zlokalizowane są tutaj dwa więzienia stanowe – California State Prison , Corcoran ( Corcoran I ) oraz California Substance Abuse Treatment Facility and State Prison , Corcoran ( Corcoran II ) .
