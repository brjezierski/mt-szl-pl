Drechmeria gunni
Drechmeria gunni ( Berk . )
Spatafora , Kepler & amp ; Quandt – gatunek grzybów z rodziny buławinkowatych ( `` Clavipitaceae '' ) .
Występuje w Australii .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Drechmeria , Clavicipitaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1848 Miles Joseph Berkeley nadając mu nazwę `` Sphaeria gunni '' .
Ten sam autor w roku 1859 nadał mu nową nazwę , przenosząc go do rodzaju `` Cordyceps '' .
W 2015 r. Spatafora , Kepler i Quandt zaklasyfikowali go do innej rodziny i nadali mu obecną nazwę .
