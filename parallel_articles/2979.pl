Morfologia .
Owocnik jest buławkowaty , 10–20 cm wysokości , oliwkowozielonej barwy ; wyrasta z zagłębionej w ziemi gąsienicy motyla .
Znaczenie .
Pasożytuje na larwach i poczwarkach ciem , najczęściej `` Acacia spp '' .
