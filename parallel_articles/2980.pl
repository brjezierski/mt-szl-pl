Maczużnik guzkowaty
Maczużnik guzkowaty ( `` Akanthomyces tuberculatus '' ( Lebert ) Spatafora , Kepler & amp ; B. Shrestha ) – gatunek grzybów z rodziny maczużnikowatych ( `` Cordycipitaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Akanthomyces , Cordycipitaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1858 r. Hermann Lebert nadając mu nazwę `` Acrophyton tuberculatum '' .
