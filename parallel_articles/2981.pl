Zabytki i atrakcje turystyczne .
W latach 1964-1965 wybudowano kościół Matki Bożej Anielskiej .
Kościół jest w stanie pomieścić 65 osób .
Każdego roku w święto patronki ( 22 sierpnia ) jest prowadzona procesja ulicami centrum miasta .
