Coreca
Coreca – dzielnica miasta Amantea , w prowincji Cosenza we Włoszech .
Znajduje się blisko granicy miasto Campora San Giovanni .
Granice i terytorium .
Coreca wychodzi na zachód na Morze Tyrreńskie i leży w gminie Amantea , na południu zaczyna się granica z Campora San Giovanni .
Obszar składa się głównie ze skalistego cyplu , na obszarze równistym leży centrum miasta .
Terytorium dzielnicy zawiera również pagórkowaty teren i szerokie plaże .
Klimat jest łagodny .
Gospodarka .
Coreca oraz sąsiednia dzielnica Campora San Giovanni czerpie dochody z hotelarstwa i turystyki .
Jest też kilka małych fabryk ( przemysł spożywczy , mechaniczny i meblowy ) .
