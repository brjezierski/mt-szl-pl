Cork
Cork ( irl .
`` Corcaigh '' , wym . )
– drugie co do wielkości miasto Irlandii , leżące przy ujściu rzeki Lee do Morza Celtyckiego , ośrodek administracyjny hrabstwa Cork i największe miasto prowincji Munster .
Nazwa miasta .
Nazwa Cork pochodzi od irlandzkiego `` Corcach Mór Mumhan '' , czyli Wielkich Moczarów Munsteru .
Nazwę tę Cork zawdzięcza rozlewiskom rzeki Lee , przy ujściu której miasto jest położone .
Centrum miasta leży na wyspie pomiędzy głównymi korytami rzeki a większość ulic powstała w wyniku zasypania dawnych kanałów rzecznych .
Historia .
Choć rejon zatoki Cork był zamieszkany w czasach prehistorycznych , to jednak na terenie miasta brak śladów osadnictwa z tego okresu .
Klasztor wzniesiony w VI lub VII w. na południowym brzegu rzeki Lee przez świętego Findbara był pierwszym dziełem rąk ludzkich na terenie dzisiejszego miasta .
Pierwsze wzmianki o klasztorze pojawiają się w kronikach z 682 roku , kiedy to nastąpiła śmierć opata klasztoru – Suibne .
