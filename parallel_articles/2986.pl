Corleone
Corleone ( sycyl .
`` Cunigghiuni '' ) – miejscowość we Włoszech , w regionie Sycylia , w prowincji Palermo .
Liczy ok. 12 000 mieszkańców .
Historia .
Miasto zostało kiedyś podbite przez Arabów , którzy pozostawili po sobie rozwiniętą ekonomię i politykę .
Nadali oni wtedy miastu nazwę `` Qurlayun '' .
W dawnych czasach miasto było otoczone murami obronnymi połączonymi z `` Castello Soprano '' i `` Castello Sottano '' .
Corleone znane było także pod nazwą „ Odważni mieszkańcy ” , z powodu ich pozycji w wojnach , które obejmowały Sycylię .
Położone pomiędzy Palermo a Agrigento miasto kontrolowało jedną z ważniejszych arterii i było jednym z najbardziej strategicznych miejsc na wyspie .
Corleone w XIV w. stało się własnością królewską , a następnie oddane feudałowi Federico Ventimiglii .
Znaczny wzrost liczby ludności w XV i XVI w. spowodowany został przez osiedlenie się w mieście mnichów z kilku różnych zakonów .
