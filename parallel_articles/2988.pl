Cornwall ( Ontario )
Cornwall – miasto ( `` city '' / `` cité '' ) w Kanadzie , w prowincji Ontario , w hrabstwie Stormont , Dundas i Glengarry .
Nazwa została nadana miastu na cześć Jerzego IV Hanowerskiego , księcia Kornwalii ( ang .
`` Cornwall '' ) .
Liczba mieszkańców Cornwall wynosi 45 965 .
Język angielski jest językiem ojczystym dla 64,2 % , francuski dla 27,2 % mieszkańców ( 2006 )
W mieście rozwinął się przemysł drzewny , papierniczy , włókienniczy , chemiczny oraz elektrotechniczny .
