Zasłonak bulwiasty
Zasłonak bulwiasty ( `` Cortinarius bulbosus '' ( Sowerby ) Gray ) – gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go James Sowerby w 1799 r. nadając mu nazwę `` Agaricus bulbosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Samuel Frederick Gray w 1821 r .
Synonimy :
Polską nazwę nadał mu F. Kwieciński w 1896 r .
Występowanie i siedlisko .
Znane jest występowanie tego gatunku w Ameryce Północnej , Europie , Azji i Afryce .
W. Wojewoda w 2003 r. przytacza tylko dwa jego stanowiska w Polsce i to dawne ( 1896 , 1955 r. ) .
W `` Czerwonej liście roślin i grzybów Polski '' ma status ex – gatunek wymarły .
Naziemny grzyb mykoryzowy .
Występuje w lasach iglastych .
