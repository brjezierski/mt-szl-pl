Zasłonak cynamonowofioletowy
Zasłonak cynamonowofioletowy ( `` Cortinarius cinnamoviolaceus '' M.M .
Moser ) – gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podają niektóre atlasy grzybów .
Morfologia .
Średnica 2–5cm , młody stożkowaty , starszy łukowaty lub płaski z tępym garbem , brzegi często popękane .
Brzeg kapelusza długo pozostaje podwinięty i połączony z trzonem gęstą , białą zasnówką .
Powierzchnia gładka , pokryta promieniście ułożonymi włókienkami i higrofaniczna ; podczas wilgotnej pogody ma kolor od purpurowego do siwobrązowego , podczas suchej jest ochrowobrązowa z fioletowym odcieniem na brzegach kapelusza .
Szerokie i szeroko przyrośnięte , grube , u młodych okazów fioletowe , u starszych rdzawobrązowe .
Ostrza blaszek białawe .
Wysokość 5–9 cm , grubość 0,8–2 cm , walcowaty , kruchy , początkowo pełny , u starszych okazów pusty , gładki .
Powierzchnia ma fioletowy kolor , ale u młodych okazów pokryta jest białą zasnówką , u starszych po odpadnięciu zasnówki trzon jest fioletowy , z wyjątkiem części podkapeluszowej , która jest biała .
Pierścienia brak .
