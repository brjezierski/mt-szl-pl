Zasłonak damasceński
Zasłonak damasceński ( `` Cortinarius damascenus '' Fr . )
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim : `` Hydrocybe damascena '' ( Fr . )
Wunsche .
Nazwę polską podał Andrzej Nespiak w 1981 r .
Morfologia .
Średnica 5–8 cm , początkowo wypukły , potem płasko rozpostarty .
Jest wyraźnie higrofaniczny ; w stanie wilgotnym ciemnobrązowy , w stanie suchym jasnobrązowy , na środku ochrowy .
Na brzegu kapelusza pozostałości srebrzystej zasnówki .
