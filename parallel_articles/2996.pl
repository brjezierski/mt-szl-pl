Zasłonak różowotrzonowy
Zasłonak różowotrzonowy ( `` Cortinarius erythrinus '' ( Fr . )
Fr . )
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go Elias Fries w 1818 r. nadając mu nazwę `` Agaricus caesius '' d `` erythrinus '' .
Ten sam autor w 1838 r. przeniósł go do rodzaju `` Cortinarius '' .
Niektóre synonimy :
Stanisław Domański w 1955 r. nadał mu polską nazwę zasłonak czerwonokasztanowaty , Andrzej Nespiak w 1981 r. zasłonak sporny .
Władysław Wojewoda w 2003 r. uznał te nazwy za nieprawidłowe i zaproponował nazwę zasłonak różowotrzonowy .
