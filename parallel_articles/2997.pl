Zasłonak brunatnoplamisty
Zasłonak brunatnoplamisty ( `` Thaxterogaster fulvo-ochrascens '' ( Rob .
Henry ) Niskanen & amp ; Liimat . )
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Thaxterogaster , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go Robert Henry w 1943 r. Nadał mu nazwę `` Cortinarius fulvoochrascens '' .
W 2022 r. Tuula Niskanen i Kare Liimatainen przenieśli go do rodzaju `` Aureonarius ''
Nazwę polską podał w swojej monografii zasłonaków Andrzej Nespiak w 1975 r. Jest niespójna z aktualną nazwą naukową .
Morfologia .
Osiąga średnicę do 12 cm .
Początkowo jest półkoliście wypukły , potem rozpłaszczony .
Powierzchnia gładka , śliska z bardzo drobnymi włókienkami .
Barwa szaro-ochrowa , ochrowobrązowa , żółtobrązowa .
U młodych owocników z kapelusza zwisają białawe resztki osłony .
