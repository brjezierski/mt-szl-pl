Zasłonak wrzecionowatozarodnikowy
Zasłonak wrzecionowatozarodnikowy ( `` Cortinarius fusisporus '' Kühner ) – gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Występowanie i siedlisko .
Znane jest występowanie tego gatunku tylko w Europie .
Na terenie Polski do 2003 r. w literaturze podano tylko jedno stanowisko .
