Zasłonak delikatny
Zasłonak delikatny ( `` Cortinarius gracilior '' Jul .
Schäff . )
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Według Index Fungorum takson niepewny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podał Andrzej Nespiak w 1975 r .
Morfologia .
Średnica 1,5–4 cm , początkowo stożkowaty , potem półkulisty , w końcu płaskowypukły .
Powierzchnia drobnowłóknista o barwie od jasno żółtobrązowej do ochrowożółtej , czasami ( rzadko ) z resztkami osłony .
Jest nieco higrofaniczny .
Przyrośnięte , początkowo białawe lub kremowe , potem rdzawe .
Wysokość 2–4 cm , grubość 4-7 mm , cylindryczny , w górnej części biały , podstawa z wyraźną bulwą .
W kształcie cytryny , wyraźnie brodawkowane , brązowawe , o rozmiarach 10–12 × 5,5–7 μm .
Wysyp zarodników rdzawy .
Występowanie i siedlisko .
Znany jest tylko w niektórych krajach Europy .
Jest bardzo rzadki .
Grzyb mikoryzowy .
Rośnie na ziemi , w lasach liściastych .
