Zasłonak gniadofioletowy
Zasłonak gniadofioletowy ( `` Calonarius rufo-olivaceus '' ( Pers . )
Niskanen & amp ; Liimat . )
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Calonarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Agaricus rufo-olivaceus '' .
Obecną nazwę nadali mu Tuula Niskanen i Kare Liimatainen w 2022 r .
Synonimy :
Nazwę polską nadał Andrzej Nespiak w 1975 r. Jest niespójna z aktualną nazwą naukową .
Morfologia .
Średnica 6–12 cm , za młodu łukowaty z podwiniętym brzegiem , potem płaski i nadal z podwiniętym brzegiem .
