Zasłonak różnobarwny
Zasłonak różnobarwny , zasłonak znamienny , zasłonak gajowy ( `` Cortinarius variicolor '' ( Pers . )
Fr .
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' )
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cortinarius , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1801 Persoon nadając mu nazwę `` Agaricus variicolor '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1838 Fries , przenosząc go do rodzaju `` Cortinarius '' .
Niektóre synonimy nazwy naukowej :
Nazwy polskie nadał Andrzej Nespiak w 1975 r. Władysław Wojewoda w swoim wykazie grzybów wielkoowocnikowych Polski wymienia także gatunek zasłonak gajowy ( `` Cortinarius nemorensis '' ) , jednak według Index Fungorum jest on synonimem `` cortinarius variicolor '' .
Morfologia .
Średnica 5-18 cm , u młodych owocników półkulisty i nieco śliski , u starszych łukowaty i suchy .
