Zasłonak piekący
Zasłonak piekący ( `` Thaxterogaster vibratilis '' ( Fr . )
Niskanen & amp ; Liimat . )
– gatunek grzybów należący do rodziny zasłonakowatych ( `` Cortinariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Thaxterogaster , Cortinariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1821 r. Elias Fries nadając mu nazwę `` Agaricus vibratlis '' .
Ten sam autor w 1838 r. , przeniósł go do rodzaju `` Cortinarius '' .
W 2022 r. Tuula Niskanen i Kare Liimatainen przenieśli go do rodzaju `` Aureonarius ''
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. , Andrzej Nespiak w 1975 r. opisywał ten gatunek jako zasłonak drżący Obydwie nazwy są niespójne z aktualną nazwą naukową .
