Morfologia .
W obrębie plam na obydwu stronach porażonych liści tworzą się blado brązowe , gładkie , cylindryczne , proste lub lekko zakrzywione konidiofory o wymiarach 153 ̶ 646 × 5 ̶ 9 µm , z 3-11 przegrodami .
Konidia powstają pojedyncze lub w krótkich łańcuchach akropetalnych , są wrzecionowate , maczugowate do niemal cylindrycznych , proste lub zakrzywione , o wymiarach 112–314 × 4–8 μm .
Mają 8 – 33 przegród , barwę od szklistej do jasnobrązowej , są gładkie , z 1-2 płaską ciemną i zgrubiałą hila .
Na pożywkach kolonie rosną stosunkowo wolno ( średnica 5,3 cm po 9 dniach ) , mają powierzchnię garbkowatą , filcowatą , brzeg cały , sino-szary do mysio szarego .
Zarodnikują rzadko .
Bardzo charakterystyczną cechą konidiów jest wnęka lub punkt przyczepienia konidioforu , zwykle wyglądający na grubszy i ciemniejszy niż reszta zarodników .
W glebie grzyb może wytwarzać chlamydospory umożliwiające mu dłuższe przetrwanie .
Mają postać grubościennych , nabrzmiałych i zmelanizowanych ( ciemnych ) strzępek .
Wykryto je w kilku roślinach żywicielskich , w tym u groszka zwyczajnego , bakłażana , ogórka , u lantany pospolitej , malpigii granatolistnej i `` Vernonia cinerea '' .
Znana jest tylko anamorfa .
Znaczenie .
`` Corynespora cassiicola '' to grzyb mikroskopijny , pasożyt , saprotrof i endofit atakujący ponad 530 gatunków roślin w 53 rodzinach .
Występuje najczęściej w tropikach i subtropikach , gdzie powoduje duże szkody ekonomiczne na plantacjach roślin uprawnych .
Został również wyizolowany z nicieni i ludzkiej skóry .
Jest patogenem wielu roślin uprawnych , m.in .
grochu zwyczajnego , papai , kauczukowca , soi , pomidora .
W Polsce wywołuje chorobę o nazwie korynesporoza dyniowatych i korynesporoza soi .
U pomidora powoduje korynesporozę pomidora .
