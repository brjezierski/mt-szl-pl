Corynespora cassiicola
Corynespora cassiicola ( Berk .
& amp ; M.A .
Curtis ) C.T .
Wei – gatunek grzybów z rzędu `` Pleosporales '' .
Grzyb pasożytniczy występujący na licznych gatunkach roślin i powodujący u nich plamistość liści .
Jest szeroko rozprzestrzeniony na całym świecie .
Systematyka .
`` Corynespora , Corynesporascaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisali Miles Joseph Berkeley i Moses Ashley Curtis w 1868 r. nadając mu nazwę `` Helminthosporium cassiicola '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu C.T .
Wei w 1950 r .
