Cosenza ( stacja kolejowa )
Cosenza ( wł .
`` Stazione di Cosenza '' ) – stacja kolejowa w Cosenza , w prowincji Cosenza , w regionie Kalabria , we Włoszech .
Położona jest na linii Cosenza – Sibari oraz Cosenza – Paola .
Jest również stacją początkową dla linii Cosenza – Catanzaro Lido obsługiwane przez Ferrovie della Calabria .
Używana jest również alternatywna nazwa stacji `` Cosenza Vaglio Lise '' .
