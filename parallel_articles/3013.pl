Courbevoie
Courbevoie – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Według danych na rok 2012 gminę zamieszkiwało 86 854 osób , a gęstość zaludnienia wynosiła 20 828 osób/km² .
Wśród 1287 gmin regionu Île-de-France Courbevoie plasuje się na 741. miejscu pod względem powierzchni .
W Courbevoie urodził się w 1914 roku aktor Louis de Funès , a w 1894 pisarz Louis-Ferdinand Céline .
Pochodzi stąd też urodzony w 1946 piosenkarz francuski Michel Delpech .
