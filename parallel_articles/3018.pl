Cowra
Cowra – miejscowość w stanie Nowa Południowa Walia położona ok. 300 km na zachód od Sydney na brzegach rzeki Lachlan .
Historia .
Przed przybyciem Europejczyków tereny przyszłego miasta zamieszkiwało aborygeńskie plemię Wiradjuri .
Pierwszym Europejczykiem , który przybył do doliny Lachlan był George Evans w 1815 , który dwa lata później uznał te obszary za nienadające się do osadnictwa , pomimo to w 1831 w regiony Cowra przybyli pierwsi osadnicy .
Pierwotnie nieoficjalna osada , która powstała około 1840 nosiła nazwę Coura Rocks , ale już w 1847 znana była jako Cowra .
