Cradle of Filth
Cradle of Filth ( z ang .
kolebka plugastwa , IPA [ ] ) – brytyjska grupa muzyczna wykonująca muzykę z pogranicza black , gothic i symfonicznego metalu .
Dziennikarze muzyczni określają styl wykonywanej przez zespół muzyki jako extreme metal lub extreme gothic metal .
Zespół powstał w 1991 roku w Suffolk .
Do 2018 roku ukazało się dwanaście albumów studyjnych Cradle of Filth oraz szereg pomniejszych wydawnictw pozytywnie ocenianych przez fanów jak i krytyków muzycznych .
Mimo częstych zmian personalnych w składzie , zespół utrzymał swój charakterystyczny styl .
Zespół dał szereg koncertów na całym świecie i uczestniczył w licznych festiwalach : Download Festival , Wacken Open Air , Gods of Metal , Tomahawk Festival , Bloodstock Open Air , Kobetasonik , Spirit of Burgas Festival oraz Ozzfest .
Został wielokrotnie nagrodzony i wyróżniony w plebiscytach muzyki heavymetalowej .
Podczas koncertów Cradle of Filth prezentuje charakterystyczny rodzaj ekspresji , obejmujący corpse paint , pokazy akrobatyczne , fire breathing , efekty pirotechniczne , nierzadko muzykom towarzyszą tancerki i performerzy .
Redaktor magazynu Metal Hammer Alexander Milas – stwierdził , że Cradle of Filth to najpopularniejsza brytyjska grupa heavymetalowa od czasów Iron Maiden .
