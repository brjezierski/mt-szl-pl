Krajowa ( rum .
`` Craiova '' ) – miasto w Rumunii ( Wołoszczyzna ) , historyczna stolica regionu Oltenia , ośrodek administracyjny i największe miasto okręgu Dolj .
Leży na lewym brzegu rzeki Jiu .
Liczy 298 928 mieszkańców ( dane z 2009 r. ) i jest szóstym co do wielkości miastem Rumunii .
Węzeł kolejowy i port lotniczy .
Historia .
Miasto zostało założone przez Daków , następnie stało się rzymskim obozem wojskowym .
Od XV w. był to znany ośrodek handlowy i siedziba namiestników Oltenii w Hospodarstwie Wołoskim .
Po okresie osłabienia ekonomicznego w XVIII w. , od początków XIX w. Krajowa odzyskała pozycję jednego z wiodących miast wołoskich .
Była ośrodkiem rzemiosła i handlu .
W 1859 wraz z Wołoszczyzną weszła w skład Zjednoczonych Księstw Mołdawii i Wołoszczyzny , które w 1866 r. przyjęły nazwę Rumunii .
