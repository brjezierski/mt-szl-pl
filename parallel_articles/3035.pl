Cremona ( stacja kolejowa )
Cremona ( wł : `` Stazione di Cremona '' ) – stacja kolejowa w Cremonie , w regionie Lombardia , we Włoszech .
Została otwarta w 1863 .
Budynek stacji jest dwupoziomowy i składa się centralnego budynku i dwóch bocznych skrzydeł .
Na parterze znajdują się usługi dla turystów , takie jak kasy i poczekalnia , a piętro jest wykorzystywane przez Trenitalia .
Istnieją dwa perony oraz cztery krawędzi peronowe .
Eksploatacji tych utworów są dwie platformy schronienie i połączone ze sobą poprzez dwa przejścia podziemne .
Perony połączone są przejściem podziemnym .
Znajduje się tu również stacja towarowa oraz lokomotywownia .
