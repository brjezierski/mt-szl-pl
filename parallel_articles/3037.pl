Ciżmówka migdałowatozarodnikowa
Ciżmówka migdałowatozarodnikowa ( `` Crepidotus caspari '' Velen . )
– gatunek grzybów należący do rodziny ciżmówkowatych ( `` Crepidotaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Crepidotus , Crepidotaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy opisał go w 1926 r. Josef Velenovský .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. dla synonimu `` Crepidotus lundellii '' .
Morfologia .
Średnica 3–20 mm , wypukłe do płaskowypukłych , okrągłe do półkolistych lub zaokrąglonych , do podłoża przyrastające bokiem lub mimośrodowo .
Zwykle występują w umiarkowanie stłoczonych grupach , przy czym sąsiednie owocniki zrastają się brzegami .
Brzeg początkowo podwinięty , potem prosty .
Powierzchnia w stanie suchym filcowata , czysto biała do bladokremowej .
Blaszki średnio gęste lub gęste , początkowo białawe , później gliniasto płowożółte , na koniec cynamonowo-brązowe , o ostrzach drobno włóknistych i stale białawych .
Podstawki 21–30 × 6–8 µm , cylindryczno-maczugowate , 4-zarodnikowe .
