Cristiano Ronaldo dos Santos Aveiro ( ur .
5 lutego 1985 w Funchal ) – portugalski piłkarz , występujący na pozycji napastnika w saudyjskim klubie Al-Nassr oraz w reprezentacji Portugalii , której jest kapitanem .
Złoty medalista Mistrzostw Europy 2016 , srebrny medalista Mistrzostw Europy 2004 .
Uczestnik Mistrzostw świata : 2006 , 2010 , 2014 , 2018 , 2022 i Mistrzostw Europy : 2008 , 2012 , 2020 .
Klubową karierę rozpoczął w wieku ośmiu lat , grając w małym klubie CF Andorinha .
W wieku 10 lat trafił do jednego z największych klubów Madery , Nacionalu , a w 1997 – do akademii Sportingu .
W 2003 podpisał kontrakt z Manchesterem United .
Już w pierwszym sezonie zdobył z klubem Puchar Anglii , natomiast w kolejnych latach pomógł United w zdobyciu trzech z rzędu tytułów mistrzowskich , dwóch Pucharów Ligi , Tarczy Wspólnoty , wygraniu Ligi Mistrzów , a także klubowego mistrzostwa świata .
W 2003 zadebiutował w seniorskiej reprezentacji Portugalii .
Od tamtego czasu wystąpił w narodowych barwach 196 razy , strzelając 118 bramek , co czyni go rekordzistą pod względem występów , a także najlepszym strzelcem reprezentacji Portugalii w historii .
W 2004 sięgnął z drużyną po srebrny medal mistrzostw Europy .
W 2008 został kapitanem reprezentacji Portugalii , którą w nowej roli poprowadził do ćwierćfinału Euro 2008 .
Cztery lata później zdobył z drużyną brązowy medal Euro 2012 , który to UEFA przyznała przegranym w półfinałach , po raz pierwszy w historii .
W 2016 zdobył z Portugalią , mistrzostwo Europy , natomiast rok później , brązowy medal Pucharu Konfederacji .
W 2009 trafił do Realu Madryt , stając się najdroższym piłkarzem w historii – transfer kosztował 94 mln euro .
Podczas oficjalnej prezentacji , zorganizowanej na Estadio Santiago Bernabeu , witało go ponad 80 tys .
kibiców .
Z hiszpańskim klubem zdobył 15 trofeów : dwa mistrzostwa Hiszpanii , dwa Puchary Króla , dwa Superpuchary Hiszpanii , cztery puchary Ligi Mistrzów , dwa Superpuchary UEFA , a także trzy klubowe mistrzostwa świata .
Z dorobkiem 450 goli jest najlepszym strzelcem w historii Realu Madryt .
Należy do niego także rekord hat-tricków w jednym ligowym sezonie w Hiszpanii – osiem z sezonu 2014/15 .
Jest jedynym piłkarzem , który zdobywał minimum 30 goli w sześciu sezonach La Liga z rzędu .
Z dorobkiem 140 goli pozostaje najlepszym strzelcem w historii rozgrywek Ligi Mistrzów UEFA .
Latem 2018 stał się piłkarzem Juventusu , transfer kosztował 100 mln euro .
W 2021 po dwunastu latach powrócił do Manchesteru United .
Po rozwiązaniu kontraktu z klubem w listopadzie 2022 był wolnym agentem na czas Mundialu w Katarze .
W grudniu 2022 został piłkarzem saudyjskiego Al-Nassr .
Laureat pięciu Złotych Piłek , czterech Europejskich Złotych Butów oraz pięciokrotny zdobywca tytułu Piłkarza Roku FIFA .
