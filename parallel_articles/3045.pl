Cristulariella depraedans
Cristulariella depraedans ( Cooke ) Höhn .
– gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Grzyb mikroskopijny , pasożyt atakujący głównie klony ( `` Acer '' ) , rzadziej niektóre inne gatunki drzew i roślin zielnych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cristulariella , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1885 r. Mordecai Cubitt Cooke nadając mu nazwę `` Polyactis depraedans '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Franz von Höhnel w 1916 r .
Synonimy :
Objawy choroby .
Patogen rozwija się na powierzchni porażonych liści .
Początkowo plamki są niewielkie ( ok. 1 mm ) , ale wraz z rozwojem choroby powiększają się .
