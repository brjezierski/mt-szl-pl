Crowborough
Crowborough - miejscowość w południowej Anglii , w dystrykcie Wealden hrabstwa East Sussex .
Miasto zajmuje powierzchnię 13,6 km2 .
Ludność miasta w 2007 roku wynosiła 19 988 osób .
Miasto położone jest na wysoczyźnie , osiągając w najwyższym punkcie 242 m ponad poziom morza .
Rozwój miejscowość zawdzięcza inicjatywie jednego z najbogatszych mieszkańców parafii Rotherfield ( do której Crowborough należało do 1880 r. ) - Sir Henry'ego Fermor .
W 1734 r. w ramach jego zapisu spadkowego ulokowano na terenie dzisiejszego Crowborough kościół pod wezwaniem Wszystkich Świętych oraz szkołę dla ubogich - oba obiekty zachowały się do dnia dzisiejszego .
Najbardziej znanym mieszkańcem miasta był Sir Arthur Conan Doyle ( 1859-1930 ) - autor powieści o Sherlocku Holmesie , którego pomnik stoi w centrum miasta .
