Cryphonectria parasitica
Cryphonectria parasitica ( Murrill ) M.E .
Barr – gatunek grzybów z rzędu `` Diaporthales '' .
Mikroskopijny grzyb pasożytniczy u kasztanów i niektórych gatunków dębów wywołujący grzybową chorobę o nazwie zgorzel kasztana .
Jest organizmem kwarantannowym podlegającym obowiązkowemu zwalczaniu .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Crypthonectria , Cryphonectriaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał William Alphonso Murrill w 1906 r. nadając mu nazwę `` Diaporthe parasitica '' .
Obecną , uznaną przez Index Fungorum nazwę nadała mu Margaret Elizabeth Barr w 1978 r .
Synonimy :
Morfologia .
`` C. parasitica '' tworzy podkładki o barwie od żółtej do ciemno pomarańczowej , średnicy 0,5–3 ( –4 ) mm i wysokości 2,5 mm .
Są zagłębione w korze , wystaje ponad nią tylko ich górna część .
W podkładkach powstają konidiomy o średnicy 100–300 μm , czasami jednak kilka konidiów łączy się z sobą tworząc duży , labiryntopodobny konidiom , którego średnica przekracza 1 mm .
Konidiofory są rozgałęzione , komórki konidiotwórcze tworzą się zarówno na szczytowych , jak i bocznych ich gałązkach .
