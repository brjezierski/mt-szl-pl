Cryptococcus neoformans
Cryptococcus neoformans ( San Felice ) Vuill .
– gatunek grzybów z rodziny trzęsakowatych ( `` Tremellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cryptococcus , Tremellaceae , Tremellales , Incertae sedis , Tremellomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Niektóre synonimy naukowe :
`` Cryptococcus neoformans '' jest znany jako przedstawiciel drożdży podstawkowych , podczas gdy jego teleomorfa opisywana była pod nazwą `` Filobasidiella neoformans '' .
Władysław Wojewoda w 2003 r. nadał temu gatunkowi nazwę polską nitkopodstawkówka wodna , ale dla synonimu `` Filobasidiella neoformans '' Kwon-Chung .
Charakterystyka .
Gatunek ten wywołuje kryptokokozę .
Jego wymiary ( bez otoczki ) wynoszą 5-10 μm .
Najważniejszym czynnikiem zjadliwości jest otoczka polisacharydowa , która chroni `` C. neoformans '' przed fagocytozą .
Dodatkowo wydzielanie melaniny , która odkłada się w ścianie komórkowej grzyba , może chronić go przed działaniem wolnych rodników wydzielanych przez fagocyty .
