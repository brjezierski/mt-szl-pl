Cuernavaca
Cuernavaca ( Nahuatl - Cuauhnāhuac ) – miasto w środkowym Meksyku , na obszarze Mesy Centralnej , około 85 km na południe od miasta Meksyk w kierunku Acapulco , stolica i największe miasto stanu Morelos .
Mieszkający w Cuernavace na początku XIX wieku niemiecki podróżnik Alexander von Humboldt nadał miastu nazwę - `` Miasto wiecznej wiosny '' .
W mieście rozwinął się przemysł spożywczy , maszynowy oraz cementowy .
W 1939 r. został założony uniwersytet .
Ośrodek turystyczno-wypoczynkowy .
