Culiacán
Culiacán , Culiacán Rosales – miasto w zachodnim Meksyku , na nizinie nadbrzeżnej , nad rzeką Culiacán .
Stolica stanu Sinaloa .
Miasto leży w dolinie , około 55 m n.p.m. , w miejscu połączenia rzek Tamazula i Humaya , tworzącej od tego miejsca rzekę Culiacán .
Miasto położone jest środkowej części stanu w podobnej odległości jak dwie inne aglomeracje : Los Mochis na północy i Mazatlán na południu .
Założone w 1531 .
Przemysł spożywczy , skórzany , cementowy , włókienniczy .
Ośrodek handlowy regionu rolniczego .
Węzeł komunikacyjny .
