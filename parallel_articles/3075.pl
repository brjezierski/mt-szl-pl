Culver City ( stacja metra )
Culver City – naziemna stacja linii Expo metra w Los Angeles znajdująca się w Culver City .
Końcowa stacja pierwszego etapu budowy linii Expo , która docelowo ma dotrzeć do miasta Santa Monica .
Została oddana do użytku 20 czerwca 2012 roku .
Opis stacji .
Stacja Culver City znajduje się na wiadukcie wzdłuż jezdni Exposition Bulevard w pobliżu skrzyżowania z Venice Boulevard .
