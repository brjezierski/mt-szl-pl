Cumaná
Cumaná – miasto w Wenezueli , stolica stanu Sucre , 305 tysięcy mieszkańców .
Jedno z najstarszych miast Ameryki Południowej ( pierwsze na kontynencie , nie licząc osadnictwa na wyspach Morza Karaibskiego ) , założone w 1515 roku , następnie kilkakrotnie przenoszone z powodu ataków Indian ( 1521 , 1569 ) .
Jego pierwotna zabudowa została zniszczona przez trzęsienia ziemi , większość zabytkowej zabudowy pochodzi z XVII - XVIII wieku .
Miasto leży u ujścia rzeki Manzanares do Morza Karaibskiego , w zatoce Cariaco .
Port rybacki ( połowy tuńczyka ) , turystyka - baza dla wycieczek do Parku Narodowego Mochima , uniwersytet .
