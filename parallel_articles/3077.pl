Cumminsiella mirabilissima
Cumminsiella mirabilissima ( Peck ) Nannf .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Pasożyt niektórych gatunków roślin z rodzaju berberys i mahonia .
W Polsce występuje tylko na mahonii pospolitej , wywołując u niej chorobę zwaną rdzą mahonii .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Uromyces , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1881 r. Charles Horton Peck nadając mu nazwę `` Puccinia mirabilissima '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu John Axel Nannfeldt w 1947 r .
Synonimy :
Morfologia i cykl życiowy .
Rdza jednodomowa – cały jej cykl życiowy odbywa się bez zmiany żywiciela .
