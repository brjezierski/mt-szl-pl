Cumnock
Cumnock ( gael .
`` Cumnag '' ) – miasto położone w Szkocji .
Leży na spływie rzek Lugar i Glaisnock .
Historia .
Cumnock było miastem górniczym , a także ośrodkiem handlowym dla mniejszych miejscowości w okolicy takich jak Lugar , Muirkirk ,
Miasto ma też silne dziedzictwo socjalistyczne ze względu na górnictwo .
W nim przez większość życia mieszkał założyciel Labour Party , Keir Hardie i jego pomnik stoi przed tutejszym ratuszem .
