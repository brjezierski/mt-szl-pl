Curepipe
Curepipe – miasto na Mauritiusie , czwarte co do wielkości miasto kraju .
Znajduje się w środkowej części wyspy .
W 2014 roku miasto zamieszkiwało 79 172 ludzi .
W mieście ma siedzibę Mauritius Broadcasting Corporation ( MBC ) , znajduje się też tutaj stadion – New George V Stadium .
Ośrodek przetwórstwa herbaty ; w pobliżu punkt widokowy z krateru wygasłego wulkanu Trou aux Cerfs .
