Curicó
Curicó – miasto i gmina w środkowym Chile , w regionie Maule , w Dolinie Środkowochilijskiej ; ośrodek administracyjny prowincji Curicó .
Gmina zajmuje powierzchnię 1328 km² , a w 2017 roku zamieszkiwało ją 149 136 mieszkańców , z czego na samo miasto przypadały 126 092 osoby .
W mieście rozwinął się przemysł spożywczy .
