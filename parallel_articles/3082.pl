Kurytyba
Kurytyba ( ) – miasto liczące razem z przedmieściami 3 miliony mieszkańców , główny ośrodek polonijny w Brazylii .
Przez wielu mieszkańców z polskimi korzeniami nazywana jest „ Chicago Ameryki Południowej ” .
Geografia .
Położona na południu Wyżyny Brazylijskiej .
W pobliżu znajdują się źródła rzeki Iguaçu .
Port lotniczy , połączenie z portem morskim ( awanportem ) o nazwie Paranaguá .
Historia .
Kurytyba została założona w 1654 roku .
