Cebula zwyczajna
Cebula ( `` Allium cepa '' L. ) – zwyczajowa nazwa warzywa należącego do rodziny amarylkowatych .
Prawidłowa nazwa botaniczna to czosnek cebula , jest to bowiem gatunek należący do rodzaju czosnek .
W użyciu jest jednak głównie nazwa cebula .
Inne używane nazwy to : cebula ogrodowa , cebula zwyczajna , dymka , skulibaba .
Historia uprawy .
Obecnie cebula nie występuje już w stanie dzikim , jako warzywo natomiast uprawiana jest niemal we wszystkich krajach .
Pochodzi prawdopodobnie z Azji Środkowej , uprawiana jest od bardzo dawna .
Rysunki cebuli znajdowane są na wykopaliskach w Palestynie pochodzących z epoki brązu sprzed około 5000 lat p.n.e .
Nie wiadomo , czy była wówczas uprawiana , czy tylko zbierana , większość naukowców jest jednak zdania , że mogła być uprawiana już wówczas : jest bowiem łatwa do uprawy i przechowywania .
Na pewno była uprawiana w Starożytnym Egipcie co najmniej 3200 lat p.n.e .
Malowidła na płaskorzeźbach egipskich oraz teksty na grobowcach wskazują , że spożywano ją w dużych ilościach .
Podczas budowy piramidy Cheopsa na zakup cebuli dla robotników wydano 1600 talentów , co było kwotą ogromną .
Resztki cebuli znajdowane są pomiędzy zwojami bandaży , którymi w Starożytnym Egipcie owijano mumie , a czasami w oczodołach mumii .
Cebula miała wówczas bardzo duże znaczenie gospodarcze , często znajduje się na egipskich wykazach podatkowych .
