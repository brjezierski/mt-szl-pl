Kubek gnojowy
Kubek gnojowy ( `` Cyathus stercoreus '' ( Schwein . )
De Toni ) – gatunek grzybów z rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1832 r. Lewis Schweinitz nadając mu nazwę `` Nidularia stercorea '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1888 r. Giovanni Battista de Toni .
