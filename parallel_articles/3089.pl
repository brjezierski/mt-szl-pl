Ssaki
Ssaki ( Mammalia ) – zwierzęta należące do kręgowców , charakteryzujące się głównie :
Około 60 % ssaków utrzymuje temperaturę w granicach 34–39 °C , 40 % wykazuje zróżnicowaną jej zmienność .
Stałocieplność umożliwia aktywny tryb życia w różnych środowiskach – od mroźnych obszarów podbiegunowych do gorących tropików .
Futro i tłuszcz pomagają uchronić się przed zimnem , a wydzielanie potu i szybki oddech pomagają pozbyć się nadmiernego ciepła .
Znaczna zmienność temperatury ciała umożliwia im natomiast ograniczenie strat ciepła do otoczenia .
Ssaki zamieszkują zarówno środowiska wodne ( walenie i brzegowce ) , jak i lądowe ( większość ) , opanowały także przestrzeń powietrzną ( nietoperze ) .
Obecnie żyje na świecie ponad 5800 gatunków ssaków .
Z wyjątkiem nielicznych jajorodnych stekowców , wszystkie współczesne ssaki są żyworodne .
Zdecydowanie największym rzędem co do liczby gatunków pośród ssaków są gryzonie , obejmujące ponad 2300 gatunków .
Następnie nietoperze , z ok. 1300 gatunkami .
Najmniejszymi liczebnie rzędami ssaków są rurkozębne oraz torbikowce mające po jednym przedstawicielu .
Liczba ssaków jest stosunkowo niewielka w porównaniu z innymi zwierzętami .
Największym żywym ssakiem i zarazem największym ssakiem w historii jest płetwal błękitny .
Osiąga on ok. 30 m długości i 180 ton wagi .
Najmniejszymi ssakami są nietoperze i ryjówki .
Nietoperz ryjkonos malutki z południowo-wschodniej Azji waży zaledwie 2 g przy długości ciała ok. 3 cm .
Ryjówek etruski waży 2 g , a jego długość łącznie z ogonem to zaledwie 4 cm .
Dział zoologii zajmujący się ssakami to teriologia .
Opieka nad młodymi oraz ich rozwój .
Wszystkie ssaki opiekują się swoimi młodymi i karmią je mlekiem .
Są jedynymi zwierzętami , które wytwarzają mleko .
Długość czasu opieki nad potomstwem waha się od kilku tygodni u myszy do wielu lat u małpy .
Niektóre ssaki , np .
koń czy bydło domowe , są zdolne po urodzeniu do samodzielnego poruszania się i odżywiania .
Budowa podniebienia twardego wraz z wargami , językiem i policzkami umożliwia ssanie .
Jad .
Przedstawiciele tylko dwóch grup ssaków wykazują się jadowitością .
Ukąszeniu niektórych ryjówek ( rzęsorki , `` Blarina brevicauda '' ) i almików towarzyszy wydzielanie niezbyt silnego jadu .
Samce dziobaka mają jadowe ostrogi na tylnych nogach .
Budowa .
Pokrycie ciała .
Większość ssaków ma ciało pokryte włosami .
Zróżnicowane są one na grube , długie włosy ościste ( szczecina dzika ) i krótkie , miękkie włosy wełniste ( owca ) .
U niektórych ssaków ( stekowce , jeżowate , jeżozwierze ) włosy są przekształcone w kolce .
U innych ( np .
kotów ) wykształcają się włosy czuciowo-zatokowe .
Linienie zachodzi najczęściej 2 razy w roku – na wiosnę i jesienią .
Ciało ssaków lub niektóre części ich ciała ( np .
u łuskowców , gryzoni ) mogą pokrywać również łuski .
Produktami naskórka są wytwory rogowe : pazury , paznokcie i kopyta .
Na spodniej stronie stóp i dłoni ssaków występują opuszki , które są zmienionymi zgrubieniami skóry .
U niektórych ssaków występują także rogi , będące tworami rogowymi oraz poroże , które jest tworem kostnym .
Skóra ssaków jest gruba i zbudowana z dwóch warstw : naskórka i skóry właściwej .
Spełnia funkcję ochronną , tworzy torbę lęgową u torbaczy , błonę lotną u nietoperzy i innych ssaków poruszających się lotem ślizgowym oraz błonę pławną u ssaków wodnych .
W skórze występują gruczoły łojowe , potowe , zapachowe i mleczne .
Szkielet .
Szkielet ssaków wykazuje duży stopień skostnienia .
Czaszka .
Kości mózgoczaszki u osobników dorosłych połączone są szwami i tworzą jednolitą puszkę , a u osobników młodych połączone są luźno błoną łącznotkankową .
Wolnymi kośćmi w czaszce są tylko kostki słuchowe , żuchwa i kość gnykowa .
Trzy kości puszki mózgowej łączą się w miejscu zwanym ciemiączkiem .
Plan budowy czaszki ssaków jest podobny do planu budowy czaszki gadów .
Przeważnie mózgoczaszka jest większa od trzewioczaszki .
Jamę nosową od ustnej oddziela kostne podniebienie .
Duża jama nosowa od przodu połączona jest z nozdrzami zewnętrznymi , które tworzą otwór gruszkowaty , a od tyłu z nozdrzami wewnętrznymi .
Kość jarzmowa i wyrostek kości skroniowej tworzą łuk jarzmowy .
Oczodół może być połączony z dołem skroniowym ( u większości ssaków ) lub całkowicie otoczony pierścieniem kostnym ( przeżuwacze , koniowate , naczelne ) .
Żuchwę buduje jedna kość , której połówki są zrośnięte lub złączone tkanką łączną .
Żuchwa łączy się stawowo z panewką kości skroniowej .
Kręgosłup i klatka piersiowa .
Kręgosłup ssaków podzielony jest na 5 odcinków : szyjny , piersiowy , lędźwiowy , krzyżowy i ogonowy .
Ssaki z wyjątkiem manatowatych i leniwców mają siedem kręgów szyjnych .
Między trzonami kręgów występują chrząstki międzykręgowe .
Pierwszy ( atlas ) , drugi ( obrotnik ) i siódmy ( kręg wystający ) kręg szyjny są kręgami nietypowymi .
Żebra połączone są z kręgami piersiowymi dwiema powierzchniami stawowymi .
Z mostkiem łączą się żebra prawdziwe .
Szkielet obręczy i kończyn .
Liczba elementów budujących obręcz barkową jest przeważnie mniejsza od liczby elementów budujących obręcz barkową gadów .
Z reguły występuje tylko łopatka z pozostałością kości kruczej w postaci wyrostka kruczego łopatki .
Jedynie u stekowców występuje kość krucza jako osobny element pasa barkowego oraz nadmostek .
Obojczyk jest dobrze rozwinięty jedynie u ssaków wykonujących kończynami ruchy odwodzące ( np .
krety , nietoperze , małpy ) .
U kopytnych i innych ssaków , które wykonują nieskomplikowane ruchy tylko w jednej płaszczyźnie , obojczyk uległ zanikowi .
Pas miedniczny , podobnie jak u gadów , zbudowany jest z jednej kości miednicznej , która powstaje ze zrośnięcia kości biodrowej , łonowej i kulszowej .
U ssaków , u których kończyny tylne zanikły ( walenie , brzegowce ) , miednica uległa redukcji .
Szkielet kończyn jest podobny do szkieletu innych kręgowców lądowych , jednak w związku z różnymi sposobami poruszania , kończyny ssaków uległy specjalizacji , która wyraża się w tendencji do skracania jednych , a wydłużania innych elementów oraz do redukcji liczby palców .
U parzystokopytnych palec I ulega uwstecznieniu , palce II i V zmniejszają się , a palce III i IV rozwijają ( typ paraksoniczny ) .
U nieparzystokopytnych zmniejszają się wszystkie palce oprócz III ( typ mezaksoniczny ) .
Nietoperze mają normalnie zbudowany tylko I palec , a pozostałe są silnie wydłużone i połączone błoną lotną .
U waleni i brzegowców następuje skrócenie kości ramieniowej , przedramienia , nadgarstka i dłoni oraz wydłużenie palców i powielenie ich liczby ( hiperdaktylia ) .
U płetwonogich dobrze rozwinięte i przystosowane do pływania są obie pary kończyn .
Uzębienie .
Zęby nie występują u kolczatki , mrówkojada , łuskowców i fiszbinowców .
Osadzone są w zębodołach kości szczękowej lub żuchwy i złożone są z jednego lub kilku korzeni oraz korony .
U niektórych ssaków zęby szczególnie narażone na ścieranie ( np .
siekacze gryzoni ) nie mają korzeni i dzięki temu wykazują nieograniczony wzrost .
