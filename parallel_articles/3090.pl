Cyclaneusma minus
Cyclaneusma minus ( Butin ) DiCosmo , Peredo & amp ; Minter – gatunek grzybów z klasy patyczniaków ( `` Leotiomycetes '' ) .
Pasożyt , jeden z patogenów wywołujących chorobę sosny ( `` Pinus '' ) o nazwie jesienna osutka sosny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cyclaneusma , Marthamycetaceae , Chaetomellales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonim : `` Naemacyclus minor '' Butin 1973 .
Morfologia .
Na obumarłych już , brązowych igłach sosny , tworzy rozproszone , eliptyczne apotecja o długości 200–600 µm , w tym samym kolorze co igła .
Gdy dojrzeją , w warunkach wilgotnej pogody pęcznieją i otwierają się pojedynczym podłużnym rozcięciem , podnosząc tkankę igły z każdej strony w jednym lub zwykle dwóch dobrze zaznaczonych płatach .
Odsłania się wówczas krążek o barwie od białej do kremowej .
Worki unitunikowe , cylindryczne , o długości 80–120 µm , szerokości 10–12 µm , zawierające osiem zarodników .
Askospory nitkowate , szkliste , dwudzielne , gładkie , o długości 65–100 µm , szerokości 2,5–3 µm , zazwyczaj lekko wygięte , jak bumerang .
