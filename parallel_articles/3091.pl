Cyclaneusma niveum ( Pers . )
DiCosmo , Peredo & amp ; Minter – gatunek grzybów należący do klasy patyczniaków ( `` Leotiomycetes '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cyclaneusma , Marthamycetaceae , Chaetomellales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1822 r. Christiaan Hendrik Persoon , nadając mu nazwę `` Stictis nivea '' .
Obecną nazwę nadali mu Frank DiCosmo , H.L .
Peredo i David William Minter w 1983 r .
Synonimy :
Morfologia .
Apotecja ( miseczki ) występujące w rozproszeniu na górnej i dolnej stronie zbrązowiałych igieł sosny .
Mają eliptyczny kształt , długość 320–950 µm i są tego samego koloru co igła .
