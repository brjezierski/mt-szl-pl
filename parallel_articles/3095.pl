Akordeon
Akordeon ( ) – instrument muzyczny zaliczany do grupy aerofonów bądź idiofonów dętych ( patrz niżej ) .
Jest rodzajem harmonii , opartym na stroikach przelotowych .
Prąd powietrza wytworzony przez ręcznie poruszany miech , wprawiając w drganie stroik wywołuje dźwięk .
Początkowo działał przy użyciu miechów nożnych , które z czasem zostały zastąpione przez miech ręczny .
Instrument ten z prawej strony ma klawiaturę klawiszową ( lub guziki w przypadku akordeonu guzikowego ) , a z lewej guziki ( dźwięki basowe , akordy ) oraz – choć nie zawsze – manuał melodyczny zwany barytonem .
Niektórzy instrumentoznawcy umieszczają akordeon w grupie idiofonów , jednak twierdzenie to jest kontrowersyjne , gdyż idiofony same w sobie są źródłem dźwięku , zaś w akordeonie stroiki wprawiane są w drgania poprzez słup powietrza – tak , jak w przypadku aerofonów .
Przyporządkowanie akordeonu do idiofonów wynika natomiast z uznania za źródło dźwięku samych stroików , a nie przepływającego przez nie powietrza .
Historia akordeonu .
Stroik przelotowy znany był już prawie 3000 lat p.n.e .
w Chinach w instrumencie o nazwie sheng , będącym rodzajem harmonijki ustnej .
Szeng zewnętrznie nawiązywał do kształtu legendarnego ptaka feniksa .
W latach 1770–1750 Bernhard Eschenbach – wraz z budowniczym fortepianów Casparem Schlimbachem zbudował instrument o nazwie eolina .
Był to instrument z klawiaturą obejmującą ponad 3 oktawy , pod którą znajdowały się dwa miechy obsługiwane kolanami .
Christian Friedrich Ludwig zbudował w 1822 r. eolinę ręczną ( niem .
handeoline ) zaopatrzoną w miech .
Najważniejszym wynalazkiem w tym urządzeniu – z punktu widzenia przyszłego rozwoju było wykorzystanie przepływu powietrza z obu kierunków pracy miecha .
W 1829 r. w Wiedniu budowniczy organów Cyrill Demian oraz jego synowie Carl i Guido opatentowali instrument o nazwie accordion .
Budowniczowie jako najważniejszą cechę instrumentu podali możliwość gry akordami , stąd jego nazwa – accordion .
Skala accordionu podana w opisie patentowym obejmowała dźwięki od g do d2 .
Sposób gry i trzymania instrumentu był odmienny od obecnego .
Prawą ręką trzymano miech , kciuk lewej ręki wkładano w skórzany pasek przymocowany do gryfu .
Miech pracował wertykalnie .
Pozostałe palce lewej ręki obsługiwały klawisze .
