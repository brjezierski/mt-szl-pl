Powłoczniczek gładki
Powłoczniczek gładki ( `` Cylindrobasidium laeve '' ( Pers . )
Chamuris ) – gatunek grzybów z rodziny obrzękowcowatych ( `` Physalacriaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cylindrobasidium , Physalacriaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1794 r. Christian Hendrik Persoon nadając mu nazwę `` Corticium laeve '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1984 r. George Peter Chamuris , przenosząc go do rodzaju `` Cylindrobasidium '' .
Niektóre synonimy naukowe :
Według polskich autorów synonimem `` Cylindrobasidium laevis '' jest także `` C. evolvens '' ( Fr . )
Jülich .
Polską nazwę nadał Władysław Wojewoda w 2003 r , wcześniej w polskim piśmiennictwie mykologicznym gatunek ten opisywany był także jako płaskosz gładki , powłocznik gładki , i zadrzak okrągły .
