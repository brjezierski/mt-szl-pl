Cyllamyces aberensis
Cyllamyces aberensis Ozkose et al .
– gatunek grzybów należący do rodziny `` Neocallimastigaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Callamyces , Neocallimastigaceae , Neocallimastigales , Incertae sedis , Neocallimastigomycetes , Incertae sedis , Chytridiomycota , Fungi '' .
Charakterystyka .
Anaeroby żyjące w jelitach zwierząt .
Po raz pierwszy zostały wyizolowane ze świeżych odchodów bydła .
Ich plecha składa się z bulwiastej stopki i wyrastających z niej rozgałęzionych sporangioforów zawierających po kilka zarodni .
Zarodniki u tego gatunku mają formę kulistych , jednokomórkowych zoospor wyposażonych w pojedynczą wić .
Od pozostałych rodzajów z rodziny `` Neocallimastigaceae '' różnią się wytwarzaniem policentrycznej plechy , brakiem chwytników oraz pojedynczą stopką .
