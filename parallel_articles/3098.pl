Ośrodkowy układ nerwowy
Ośrodkowy układ nerwowy , centralny układ nerwowy ( łac .
`` systema nervosum centrale '' ) , OUN , CUN , CNS ( od ang .
`` central nervous system '' ) – najważniejsza część układu nerwowego kręgowców .
Ośrodkowy układ nerwowy jest chroniony przez kości czaszki oraz kręgosłup .
Zbudowany jest z istoty szarej i białej .
Częścią składową istoty szarej są komórki nerwowe , a także włókna nerwowe rdzenne i bezrdzenne , tkanka glejowa i naczynia krwionośne wraz z paskami tkanki łącznej .
Skład istoty białej to tkanka glejowa , naczynia włókien nerwowych niemających osłonki Schwanna .
