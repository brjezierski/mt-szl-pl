Cypr ( wyspa )
Cypr – azjatycka wyspa we wschodniej części Morza Śródziemnego , często traktowana jako część Bliskiego Wschodu .
Czasami bywa jednak zaliczana do Europy , gdyż historycznie , kulturowo i politycznie stanowi jej część .
Jest trzecią pod względem wielkości wyspą na Morzu Śródziemnym po Sycylii i Sardynii .
Geografia .
Cypr jest położony około 65 km na południe od Turcji , 100 km na zachód od Syrii i 770 km na południowy wschód od kontynentalnej Grecji .
Wyspa rozciąga się na długości 225 km , od przylądka Arnauti na zachodzie do przylądka Apostolos Andreas na północnym wschodzie , a jej rozciągłość w osi północ-południe wynosi 100 km .
Długość linii brzegowej wynosi około 640 km .
W północnej części wyspy znajdują się równoleżnikowo ułożone Góry Kyreńskie , zaś na południu i południowym zachodzie położone są góry Trodos , których najwyższym szczytem jest Olimbos ( 1951 m ) , będący także najwyższą górą wyspy .
Między pasmami gór rozciąga się równina Mesaoria .
Największą rzeką wyspy jest wypływający z gór Trodos Pedieos , który uchodzi do Zatoki Famagusta .
Warunki klimatyczne są typowe dla obszaru Morza Śródziemnego : latem jest gorąco i słonecznie , zimą deszczowo .
Rzeźba terenu jest urozmaicona , gleby urodzajne .
Warunki zewnętrzne sprzyjają uprawie oliwek , tytoniu i winogron , z których produkuje się wina .
Cypryjczycy jednak największe zyski czerpią z turystyki .
Większość wyspy wchodzi w skład Republiki Cypryjskiej , jednakże na północy kraju jest Republika Cypru Północnego uznawana tylko przez Turcję .
Na południu wyspy znajduje się niewielkie terytorium zamorskie Wielkiej Brytanii : Akrotiri i Dhekelia .
Historia .
W czasie macedońskiej wojny domowej doszło tam do bitwy ( 306 r .
p.n.e . ) .
W starciu tym Demetriusz I Poliorketes , na czele 150 okrętów , w tym eskadry ateńskiej , powstrzymał flotę Ptolemeusza , składającą się ze 140 okrętów wojennych i 200 transportowych wiozących 10 000 piechoty .
Ptolemeusz uciekł z 8 okrętami .
Po bitwie poddały się jego państwa na Cyprze ( za Małą Encyklopedią Wojskową ) .
Większość mieszkańców Cypru stanowią Grecy .
Turcy stanowią około 1/5 ludności .
Od schyłku epoki kolonialnej Grecy walczyli o , popierane przez Grecję , zjednoczenie Cypru z Grecją ( tzw .
Enosis ) , co wywołało konflikty z ludnością turecką , która w odpowiedzi proponowała przyłączenie Cypru do Turcji lub pozostawienie go kolonią brytyjską , później zaś podział Cypru na dwie części inkorporowane następnie do Turcji i Grecji ( tzw .
Taksim ) .
W 1925 roku wyspa uzyskała status brytyjskiej kolonii .
Pod koniec lat 50 .
XX wieku gdy kolejne starania przyłączenia wyspy do Grecji nie uzyskały poparcia na forum międzynarodowym , Grecy cypryjscy opowiedzieli się za niepodległością wyspy .
W wyniku porozumienia Wielkiej Brytanii , Grecji , Turcji i Cypryjczyków w 1960 roku Cypr został uznany przez ONZ za niezależną republikę .
Konstytucja gwarantowała niezależność wyspy i zabraniała Grekom włączenia jej do Grecji .
Kilka lat później warunek ten został złamany i parlament przegłosował ustawę , w wyniku której wybuchła wojna domowa pomiędzy cypryjskimi Grekami a Turkami .
W konflikt włączyła się Turcja , która w 1974 roku dokonała inwazji na wyspę , a w 1983 roku ogłosiła utworzenie Tureckiej Republiki Cypru Północnego .
Nie została ona uznana przez żadne inne państwo , ale istnieje do dziś , dzieląc Cypr na dwie części .
Zabytki .
Cypr obfituje w zabytki , znajdujące się zarówno w strefie greckiej , jak i tureckiej .
Na tureckiej północy warto zwiedzić m.in .
ruiny Salaminy pod miastem Famagusta , samą Famagustę z ufortyfikowanym zamkiem i licznymi zabytkami architektonicznymi , górskie zamki św .
Hilariona , Kantara oraz Buffavento niedaleko Kirenii i wiele innych .
W części greckiej turyści chętnie odwiedzają Larnakę , powstałą w XIII wieku p.n.e. , ruiny starożytnych miast Pafos , Kurion , Amathus i wiele innych miejsc .
