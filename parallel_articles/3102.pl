Reformy cyrylicy dokonano w Rosji na początku XVIII wieku za sprawą Piotra Wielkiego .
Projekt reformy z osobistymi uwagami cara pochodzi z roku 1709 .
Początkowo zreformowanego pisma używano wyłącznie w kancelarii carskiej dla zapisu oficjalnych dokumentów państwowych , później jej użycie rozszerzyło się także na inne sfery piśmiennictwa , najpierw rosyjskiego , a następnie także południowosłowiańskiego .
