Cyrylica
Cyrylica – pismo alfabetyczne służące do zapisu języków wschodniosłowiańskich , większości południowosłowiańskich i innych .
Nazwa nawiązuje do dwóch braci św .
Cyryla i św .
Metodego , którzy utworzyli nowy alfabet ( zwany cyrylicą ) który zastąpił stare pismo słowiańskie ( runiczne abecadło ) .
Do zapisu tego języka zostały stworzone dwa alfabety – najpierw głagolica , za twórcę której uznawany jest Cyryl , a potem cyrylica , której wynalezienie przypisuje się bułgarskiemu uczniowi Metodego .
Historia .
Cyrylica została oparta na greckiej majuskule .
Za twórcę cyrylicy uznaje się któregoś z uczniów Cyryla i Metodego , przy czym najczęściej przywołuje się tu imiona Klimenta Ochrydzkiego bądź Konstantyna Presławskiego .
Pierwsze ślady użycia cyrylicy pochodzą z terenów wschodniej Bułgarii .
Za najstarszy datowany zabytek cyrylicki uważana jest dobrudżańska inskrypcja cara Piotra , datowana na 943 rok .
W ciągu X–XII w. cyrylica rozprzestrzeniła się z Bułgarii na tereny Serbii oraz Rusi Kijowskiej .
Cyrylica używana współcześnie dla zapisu rozmaitych języków jest alfabetem częściowo zreformowanym i unowocześnionym w stosunku do wersji pierwotnej i nosi nazwę `` grażdanki '' ( ros .
