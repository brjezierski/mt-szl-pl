Ziarnówka górska
Ziarnówka górska ( `` Cystoderma fallax '' ( A.H. Sm .
& amp ; Singer ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim : `` Cystoderma carcharias '' var .
`` fallax '' ( A.H. Sm .
& amp ; Singer ) I. Saar .
Nazwę polską nadał Władysław Wojewoda w 1999 r .
Morfologia .
O szerokości 1,5–4 cm , początkowo wypukły , potem płasko wypukły , na koniec płaski , czasami z niskim garbkiem .
Brzeg początkowo podwinięty i osłoną połączony z trzonem , potem wyprostowany , ze zwieszającymi się resztkami osłony .
Powierzchnia o barwie od rdzawobrązowej do żółtobrązowej .
Początkowo pokryta jest granulkami , lub małymi , wyprostowanymi łuskami , które jednak z czasem znikają i powierzchnia staje się gładka .
Wąskie , ściśnięte , przyrośnięte , początkowo białe , potem kremowe do bladożółtych .
