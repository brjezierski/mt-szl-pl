Ziarnówka winnopurpurowa
Ziarnówka winnopurpurowa ( `` Cystoderma superbum '' Huijsman ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
O szerokości 2-5 cm , wyjątkowo do 7 cm , początkowo wypukły , potem płasko wypukły , na koniec płaski .
Powierzchnia o barwie bordowej lub winnopurpurowej , początkowo pokryta jest granulkami , lub małymi , wyprostowanymi łuskami , które jednak z czasem znikają i powierzchnia staje się gładka .
Wąskie , ściśnięte , przyrośnięte , początkowo białe , potem bladoczerwone , mięsne .
Brak pierścienia , występuje tylko strefa pierścieniowa .
Powyżej tej strefy trzon gładki , o barwie winnobrązowej z fioletowym odcieniem , poniżej pokryty łuseczkami , początkowo białymi , potem kremowymi , w końcu bladożółtymi .
Blady , bez wyraźnego zapachu i smaku .
