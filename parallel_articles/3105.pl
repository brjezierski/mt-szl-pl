Cystolepiota sistrata
Cystolepiota sistrata ( Fr . )
Singer ex Bon & amp ; Bellù – gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cystolepiota , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1821 r. Elias Fries , nadając mu nazwę `` Agaricus sistratus '' .
W 1872 r. Lucien Quélet przeniósł go do rodzaju `` Lepiota '' , a w 1985 r. Rolf Singer , Marcel Bon i Francesco Bellù do rodzaju `` Cystolepiota '' .
Występowanie i siedlisko .
`` Cystolepiota sistrata '' występuje w Ameryce Północnej , Azji , Europie , Afryce i Australii .
Najwięcej stanowisk podano w Europie .
