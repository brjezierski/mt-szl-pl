Język czamalalski
Język czamalalski ( nazwa własna : Чамалалдуб мичIчI ) – jeden z niewielkich języków kaukaskich , używany przez Czamalalów .
Należy do języków andyjskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Czamalalski jest blisko spokrewniony z językiem bagulalskim , oraz tindyjskim .
Dane mówiące o liczbie osób posługujących się tym językiem są bardzo rozbieżne i niepewne .
Według statystyk w 1926 r. języka tego używało 3438 osób .
W późniejszym czasie Bagulalowie uznawani byli za odłam Awarów i tak też zapisywani podczas kolejnych spisów powszechnych .
