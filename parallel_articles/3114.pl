Czarnucha ( sielsowiet Hoża )
Czarnucha ( biał .
`` Чарнуха '' , ros .
`` Чернуха '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Hoża .
Dawniej chutor , a następnie folwark .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Hoża .
Według Powszechnego Spisu Ludności z 1921 roku ówczesny folwark zamieszkiwał 3 osoby , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Były tu 1 budynek mieszkalny .
Wieś należała do parafii świętych apostołów Piotra i Pawła w Hoży .
Czarnucha podlegała pod Sąd Grodzki i Okręgowy w Grodnie ; najbliższy urząd pocztowy mieścił się w także w Grodnie .
