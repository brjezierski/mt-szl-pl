Czarnówko ( Galwiecie )
Czarnówko – część wsi Galwiecie w Polsce , położona w województwie warmińsko-mazurskim , w powiecie gołdapskim , w gminie Gołdap .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
Miejscowość położona jest w odległości kilkuset metrów na południe od granicy polsko-rosyjskiej .
Na południe w bezpośrednim sąsiedztwie utworzono rezerwaty przyrody Mechacz Wielki i Czarnówko .
Obecnie w miejscowości nie ma zabudowy .
