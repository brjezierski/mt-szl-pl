Czartowczyk
Czartowczyk – wieś w Polsce położona w województwie lubelskim , w powiecie tomaszowskim , w gminie Tyszowce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
We wsi znajduje się kapliczka św .
Jana Nepomucena .
Historia .
Według spisu miast , wsi , osad Królestwa Polskiego z roku 1827 , wieś w .ówczesnej gminie Tyszowce , parafii Wożuczyn .
Posiadała wówczas 33 domy i 172 mieszkańców .
