Czas
Czas – wielkość fizyczna określająca kolejność zdarzeń oraz odstępy między zdarzeniami zachodzącymi w tym samym miejscu .
Pojęcie to jest również przedmiotem rozważań filozoficznych .
Czas może być rozumiany jako :
Koncepcje czasu .
W fizyce .
W fizyce klasycznej jest samodzielną wielkością niezależną od innych wielkości biegnącą w takim samym rytmie w całym Wszechświecie .
W mechanice relatywistycznej czas stanowi czwartą współrzędną czasoprzestrzeni , jego upływ zaś zależy od obserwatora i jest różny dla różnych obserwatorów .
Według Newtona istnieje tylko jeden , uniwersalny i wszechobejmujący czas – płynie on w jednostajnym tempie i nic nie wywiera na niego wpływu .
Jest więc absolutny i obiektywnie jednakowy w całym wszechświecie .
Leibniz sprzeciwiał się teorii Newtona utrzymując , że czas jest porządkiem następstwa zdarzeń zachodzących w świecie – bez świata ( zdarzeń ) nie może być mowy o żadnym czasie .
Także George Berkeley oponował , iż bez umysłu , który by postrzegał ruch , sama idea czasu staje się zwykłą iluzją .
Szczególna teoria względności Einsteina wprowadziła do fizyki nowe pojęcie czasu .
Okazało się , że upływ czasu zależny jest od prędkości układu odniesienia , w którym się go mierzy .
Jeżeli znajdujemy się na powierzchni Ziemi i mija nas przyjaciel w statku kosmicznym poruszającym się z prędkością porównywalną do prędkości światła w próżni , wydaje nam się , że jego zegar chodzi wolniej od naszego ( dylatacja czasu ) .
W życiu codziennym różnice te są niemierzalne , jednak w przypadku układów poruszających z prędkościami zbliżonymi do prędkości światła w próżni różnica pomiaru jest już bardzo duża .
Ogólna teoria względności Einsteina uwzględnia związek czasu z polem grawitacyjnym .
Czas nie płynie już odrębnie , lecz jest związany jako czwarty wymiar w pojęciu czasoprzestrzeni .
