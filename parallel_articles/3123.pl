Czeboksary
Czeboksary ( , ) – miasto w Federacji Rosyjskiej , stolica Republiki Czuwaszja .
Położone na prawym brzegu Wołgi , na trasie pomiędzy Kazaniem a Niżnym Nowogrodem , 768 km na wschód od Moskwy .
Liczba mieszkańców miasta wynosi 497 618 ( 2020 ) .
Historia .
Obszar , na którym położone jest miasto , zasiedlony był najprawdopodobniej już w XIII wieku .
Dopiero jednak w 1555 roku powstał tu ruski ośrodek obronny .
Na koniec XVII wieku przypadło zwiększenie znaczenia miejscowości , jako jednego z ośrodków handlowych na Powołżu .
Czeboksary otrzymały status miasta w 1781 r. Należały wtedy do guberni kazańskiej .
Następne lata nie przyniosły większych oznak rozwoju miasta .
Na początku XX wieku straciło ono swoje handlowe znaczenie , stając się wtedy raczej centrum pielgrzymkowym , ze względu na dużą liczbę cerkwi i rozwinięte życie monasterskie .
W 1920 roku Czeboksary ogłoszono centrum Czuwaskiego Obwodu Autonomicznego .
Pierwsze lata istnienia państwa radzieckiego nie były jednak łatwe zarówno dla Czeboksar , jak i całej Czuwaszji .
W 1922 roku obszary te nawiedziła tragiczna w skutkach klęska głodu .
Wkrótce jednak tempo rozwoju miasta zwiększyło się .
W 1925 roku stało się ono stolicą Czuwaskiej ASRR .
Od 1992 roku Czeboksary są stolicą Republiki Czuwaszja , jednego z 89 podmiotów Federacji Rosyjskiej .
Gospodarka .
W mieście rozwinął się przemysł maszynowy , metalowy , elektrotechniczny , włókienniczy , chemiczny , drzewny , spożywczy oraz materiałów budowlanych .
