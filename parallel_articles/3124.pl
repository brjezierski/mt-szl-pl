Gmina .
Miejscowości gminy : miasto Czechowice-Dziedzice , Zabrzeg , Ligota , Bronów .
Powierzchnia gminy : 66 km² .
Ludność gminy w 2011 r. : 43 920 mieszkańców ( w tym 35 061 w mieście `` Czechowice-Dziedzice '' ) .
Podział administracyjny .
W mieście Czechowice-Dziedzice wydzielono oficjalnie 9 osiedli , które stanowią jednostki pomocnicze gminy .
Mieszkańcy rozróżniają jednak inne dzielnice , takie jak : Manhattan , Żebracz ( Kopalnia ) , Grabowice , Podkępie , Ochodza , Świerkowice , Kamionka .
Zasoby naturalne .
Na terenie Czechowic-Dziedzic znajdują się złoża węgla kamiennego co spowodowało powstanie kopalni `` KWK Silesia '' .
W pobliżu znajdują się również pokłady solanki ,
wykorzystywane dziś w uzdrowisku w Goczałkowicach .
W pobliżu znajduje się również zapora na Wiśle tworząc sztuczny zbiornik wodny – `` Zbiornik Goczałkowicki '' .
Ochrona przyrody .
Na terenie gminy znajduje się rezerwat przyrody Rotuz i obszar Natura 2000 Dolina Górnej Wisły , której obszar w okresie lęgowym zasiedla co najmniej 1 % populacji krajowej następujących gatunków ptaków : bączek , bąk , dzierzba czarnoczelna , mewa czarnogłowa , rybitwa białowąsa , rybitwa rzeczna , rybitwa czarna , szablodziób , ślepowron , cyranka , czernica , kokoszka , krakwa , krwawodziób , perkoz dwuczuby , płaskonos , sieweczka rzeczna , mewa śmieszka , zausznik .
Na terenie gminy występują też 24 pomniki przyrody , w tym 23 drzewa pojedyncze i 1 aleja złożona z lip drobnolistnych .
