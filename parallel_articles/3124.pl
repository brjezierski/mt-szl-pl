Czechowice-Dziedzice
Czechowice-Dziedzice ( , `` Czechowitz-Dziedzitz '' ; do 21 listopada 1958 `` Czechowice '' ) – miasto w południowej Polsce , w województwie śląskim , w powiecie bielskim , siedziba gminy miejsko-wiejskiej Czechowice-Dziedzice ; miasto leży historycznie na Górnym Śląsku w aglomeracji bielskiej .
Miasto powstało w 1951 ( de facto w 1941 ) pod nazwą `` Czechowice '' ( do 1958 ) z połączenia wsi Czechowice i wsi Dziedzice , których historia sięga średniowiecza .
Dziedzice od końca XIX w. rozwijały się też jako węzeł kolejowy oraz ośrodek przemysłu .
Współcześnie oprócz Czechowic i Dziedzic częściami miasta są m.in .
Lipowiec , Ochodza , Olszyna , Renardowice , Żebracz i kilka mniejszych osiedli .
Czechowice-Dziedzice stanowią ośrodek przemysłowy z kopalnią węgla kamiennego , walcownią metali , fabryką sprzętu elektrotechnicznego oraz rafinerią .
W roku 2020 zlikwidowano fabrykę zapałek , która działała od roku 1919 .
Była to ostatnia polska czynna fabryka zapałek .
Według danych z 2017 r. miasto miało 35 631 mieszkańców .
Jest to trzecie pod względem liczby ludności miasto w Polsce niebędące siedzibą powiatu ( po Rumi i Knurowie ) oraz największe miasto niepowiatowe na terytorium gminy miejsko-wiejskiej .
Geografia .
Położenie miasta .
Czechowice-Dziedzice leżą w południowej części województwa śląskiego , w północnej części powiatu bielskiego , we wschodniej części gminy Czechowice-Dziedzice .
Miasto leży w historycznych granicach regionu Śląska Cieszyńskiego .
Miasto jest położone na lewym brzegu rzeki Białej przy jej ujściu do Wisły , w mezoregionie Dolina Górnej Wisły będącym częścią Kotliny Oświęcimskiej .
