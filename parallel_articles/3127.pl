Czeczewo ( województwo zachodniopomorskie )
Czeczewo ( niem .
`` Rotharm '' ) – osada leśna w Polsce położona w województwie zachodniopomorskim , w powiecie myśliborskim , w gminie Myślibórz .
Miejscowość wchodzi w skład sołectwa Kierzków .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gorzowskiego .
