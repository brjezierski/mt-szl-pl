Czepielin
Czepielin – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mordy .
Wierni Kościoła rzymskokatolickiego należą do parafii Apostołów Szymona i Judy Tadeusza w Czołomyjach .
Miejscowość wzmiankowana w 1408 jako uposażenie parafii w Mordach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
We wsi działa założona w 1928 roku jednostka ochotniczej straży pożarnej .
Jednostka jest w posiadaniu pojazdu marki Volvo .
Inna z legend przytoczona przez księdza Byczyńskiego mówi o powstaniu nazw okolicznych miejscowości :
`` Znana szeroko legenda odnosi się raczej do wyprawy Leszka Czarnego w 1282 roku , który miał zatrzymać się obozem na wzniesieniu nad jeziorem , za którym znajdowała się osada , ale mając szczupłe siły , modlił się do św .
Michała Archanioła , patrona rycerzy chrześcijańskich , o pomoc .
Skoro zasnął , we śnie stanął przed nim św .
Michał z mieczem ł zapewnił o zwycięstwie .
Umocniony tym Leszek wyruszył naprzód i w pobliżu stoczył zwycięską bitwę z Jadźwingami , a miejsce to nazwano `` wojną '' i powstał Wojnów .
Po bitwie rycerze cofnęli się ku zachodowi i spoczęli na piaszczystej polanie , chłodząc uznojone czoła w pobliskich źródłach , więc miejsce to nazwano `` Czołomyje '' .
A jeszcze dalej ku zachodowi na dębach powieszano pojmanych wrogów , więc powstała nazwa `` Czepielin '' .
