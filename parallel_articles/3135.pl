Obwód czerkaski
Obwód czerkaski ( ) – jeden z 24 obwodów Ukrainy .
Leży w środkowej części Ukrainy , nad Dnieprem .
Stolicą obwodu są Czerkasy .
Obwód graniczy na północy z obwodem kijowskim , na południu z kirowohradzkim , na wschodzie z połtawskim , a na zachodzie z winnickim .
Historycznie leży na Ukrainie ( część wschodnia ) i Podolu ( część zachodnia ) .
Gospodarka .
Obwód czerkaski jest regionem rolniczo-przemysłowym .
Przemysł spożywczy stanowi 39,6 % produkcji obwodu , chemiczny 26,3 % , produkcja i dystrybucja energii elektrycznej , gazu i wody 12 % , przemysł celulozowo-papierniczy 7,4 % .
Głównymi produktami przemysłu chemicznego są nawozy azotowe , włókna sztuczne , tkaniny z włókien sztucznych .
W rolnictwie : buraki cukrowe ( ponad 8 % ogólnej produkcji na Ukrainie ) , ziemniaki , mięso , mleko , warzywa i owoce oraz zboża .
Bogactwa naturalne .
Wydobywa się tu głównie granity , glinę ( jedne z największych na Ukrainie pokłady gliny bentonitowej ) i piaskowce , wykorzystywane w przemyśle budowlanym .
Są również pokłady węgla brunatnego , torfu i boksytów , a także wód mineralnych .
Obwód zainteresowany jest napływem inwestycji zagranicznych ( przemysł przetwórczy i produkcji artykułów rolno – spożywczych , usługi , w tym turystyczne , budownictwo ) .
Skład narodowościowy .
Obwód zamieszkuje prawie 130 narodowości .
W głównej mierze są to Ukraińcy , ale żyją tu również Rosjanie , Mołdawianie , Białorusini , Tatarzy , Żydzi , Ormianie , Bułgarzy , Polacy i inni .
