Czerwonogród ( stacja kolejowa )
Czerwonogród ( ukr : `` Станція Червоноград '' ) – stacja kolejowa w Czerwonogrodzie , w obwodzie lwowskim , na Ukrainie .
Jest częścią administracji Kolei Lwowskiej .
Znajduje się na linii Lwów – Kowel .
Stacja obsługuje głównie ruch regionalny między Lwowem i Rawą Ruską .
Historia .
Stacja została otwarta 23 listopada 1887 wraz z linią Jarosław-Sokal .
Stacja nazywała się Krystynopol .
Obecna nazwa pochodzi z 1964 roku .
Na stacji zatrzymuje się pociąg dalekobieżny Kijów-Lwów i Lwów-Kijów .
Przez stację przechodzi także sześć par podmiejskich pociągów ( do Lwowa , Sokala , Rawy Ruskiej i Kowla ) .
