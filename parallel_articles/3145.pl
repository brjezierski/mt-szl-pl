Czeremcha ( szczyt )
Czeremcha ( 1146 m ) – zalesiony , niewybitny szczyt w Beskidzie Sądeckim , w Paśmie Radziejowej .
Podawana na niektórych mapach wysokość 1124 pochodzi od węzła szlaków znajdującego się jednak znacznie poniżej szczytu .
Czeremcha znajduje się w bocznym grzbiecie opadającym z Przehyby .
Jest to szczyt zwornikowy , ponieważ odbiega stąd na zachód grzbiet Czeremchy Zachodniej .
Na południowy zachód od wierzchołka jest ponadto drugi punkt zwornikowy , gdzie grzbiet ( ten biegnący z Przehyby ) rozgałęzia się na dwie odnogi : zachodnią z kulminacją Koszarki oraz południową ze szczytem Łysiny .
Północne stoki Czeremchy opadają do doliny Sopotnickiego Potoku , południowe do Sielskiego Potoku , a zachodnie do Jastrzębiego Potoku .
