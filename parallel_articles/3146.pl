Czerniachowsk ( baza lotnicza )
Baza lotnicza w Czerniachowsku – baza lotnictwa morskiego Floty Bałtyckiej Marynarki Wojennej Rosji , położona 4 km na południowy zachód od Czerniachowska w obwodzie kaliningradzkim .
W czasach ZSRR była bazą bombowców strategicznych .
Współcześnie pełni funkcję lotniska rezerwowego , gotowego przyjąć przebazowane bombowce strategiczne .
