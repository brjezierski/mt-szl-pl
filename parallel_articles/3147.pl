Czerniaków ( Białoruś )
Czerniaków , Czerniakowo ( , `` Czarniakawa '' ; , `` Czerniakowo '' ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie bereskim , w sielsowiecie Siehniewicze .
W 2009 roku liczyła 2 mieszkańców .
