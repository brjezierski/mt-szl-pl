Historia .
W XIX i w początkach XX w. osada cerkiewna i folwark położone w Rosji , w guberni grodzieńskiej , w powiecie prużańskim , w gminie Czerniakowo , której zarząd mieścił się w osadzie .
W dwudziestoleciu międzywojennym dwa folwarki leżące w Polsce , w województwie poleskim , w powiecie prużańskim , do 22 stycznia 1926 w gminie Czerniaków , po jej zniesieniu w gminie Międzylesie i od 1 kwietnia 1932 w gminie Siechniewicze .
W 1921 Czerniaków I liczył 6 mieszkańców , zamieszkałych w 1 budynku , wyłącznie Polaków wyznania rzymskokatolickiego .
Czerniaków II liczył zaś 21 mieszkańców , zamieszkałych w 4 budynkach w tym 20 Białorusinów i 1 Polaka .
20 mieszkańców było wyznania prawosławnego i 1 rzymskokatolickiego .
Po II wojnie światowej w granicach Związku Sowieckiego .
Od 1991 w niepodległej Białorusi .
