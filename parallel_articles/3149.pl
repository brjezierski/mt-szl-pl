Czerniejew
Czerniejew – ( daw .
Czerniejów ) wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Skórzec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
We wsi działa , założona w 1922 , jednostka Ochotniczej Straży Pożarnej .
Jednostka jest w posiadaniu wozów marki Jelcz i FSC Lublin .
