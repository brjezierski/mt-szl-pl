Czernichów ( Łagów )
Czernichów ( niem .
`` Hartte Vorwerk '' ) – przysiółek wsi Łagów w Polsce , położony w województwie lubuskim , w powiecie świebodzińskim , w gminie Łagów , nad jeziorem Czarnym .
Do 1945 roku był to niemiecki folwark .
W latach 1975–1998 przysiółek należała administracyjnie do województwa zielonogórskiego .
