Obwód czernihowski
Obwód czernihowski ( ) – jeden z 24 obwodów Ukrainy .
Leży w północnej części Ukrainy , na północy graniczy z Białorusią i Rosją , na wschodzie z obwodem sumskim , na południowym wschodzie z obwodem połtawskim , na południu i zachodzie z obwodem kijowskim .
Obwód został utworzony 7 października 1932 .
Stolicą obwodu jest Czernihów .
Historia .
W czasach przynależności do Królestwa Polskiego magdeburskie prawa miejskie z rąk Wazów otrzymały m.in .
Pryłuki , Nowogród Siewierski , Czernihów , Nieżyn , Borzna i Ostrz .
Czernihów był stolicą województwa czernihowskiego , położonego w prowincji małopolskiej Korony Królestwa Polskiego .
Obszar obwodu leżał w granicach Polski do 1667 r. , a utratę potwierdził pokój Grzymułtowskiego w 1686 r .
