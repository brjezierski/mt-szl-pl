Obwód czerniowiecki
Obwód czerniowiecki ( , ) – jeden z 24 obwodów Ukrainy .
Leży w południowo-zachodniej części Ukrainy , na południu graniczy z Rumunią i Mołdawią , na zachodzie i północnym zachodzie z obwodem iwanofrankiwskim , na północy z obwodem tarnopolskim i chmielnickim , na wschodzie z obwodem winnickim .
W dwudziestoleciu międzywojennym jako jedyny obwód znajdował się w granicach Rumunii .
Stolicą obwodu są Czerniowce .
Historia .
Obwód został utworzony w 1940 roku z północnej części Bukowiny oraz części Besarabii ( razem z Chocimiem ) oraz regionu Herca anektowanych od Królestwa Rumunii .
