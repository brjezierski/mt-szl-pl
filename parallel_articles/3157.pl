Śląsk Czeski
Śląsk Czeski ( też `` Śląsk Morawski '' , dawniej `` Śląsk Austriacki '' , `` Śląsk Czechosłowacki '' lub `` Śląsk Sudecki '' , czes .
`` České Slezsko '' , czasami `` Moravské Slezsko '' , niem .
`` Tschechisch Schlesien '' , `` Mährisch-Schlesien '' albo `` Sudetenschlesien '' ) – część Śląska należąca dziś do Czech .
W skład Śląska Czeskiego wchodzą :
Do Śląska Czeskiego zalicza się dziś również 26 enklaw morawskich , które już od XIX wieku administracyjnie tworzyły całość z dawnym Śląskiem Austriackim .
Większa część Śląska czeskiego znajduje się w obrębie kraju morawsko-śląskiego , mniejsza część leży w kraju ołomunieckim .
Fragmenty Dolnego Śląska otrzymane w 1958 znajdują się w kraju hradeckim i libereckim .
Stolica : Opawa ( czes .
`` Opava '' ) .
Największym miastem położonym w całości na Śląsku Czeskim jest Hawierzów , natomiast największym miastem leżącym w ponad 60 % na tej historycznej ziemi jest Ostrawa .
