Czechy ( obwód miński )
Czechy ( biał .
`` Чахі '' , ros .
`` Чахи '' ) – wieś na Białorusi , w obwodzie mińskim , w rejonie mołodeczańskim , w sielsowiecie Chożów .
Historia .
W czasach zaborów w gminie Horodek , w powiecie wilejskim , w guberni wileńskiej Imperium Rosyjskiego .
W 1866 roku liczyła 25 dusz rewizyjnych .
Należała do dóbr Soroczyn i Duszczyce .
W latach 1921–1945 wieś leżała w Polsce , w województwie wileńskim , w powiecie wilejskim , od 1927 roku w powiecie mołodeczańskim , w gminie Gródek .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 47 osób , 21 były wyznania rzymskokatolickiego a 26 prawosławnego .
Jednocześnie wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Było tu 8 budynków mieszkalnych .
W 1931 w 13 domach zamieszkiwało 71 osób .
Wierni należeli do parafii rzymskokatolickiej i prawosławnej w Chołchle .
Miejscowość podlegała pod Sąd Grodzki w Rakowie i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Gródku .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
Od czerwca 1941 roku pod okupacją niemiecką .
W 1944 miejscowość została ponownie zajęta przez wojska sowieckie i włączona do Białoruskiej SRR .
Od 1991 w składzie niepodległej Białorusi .
