Czesław Gęborski
Czesław Gęborski ( ur .
5 czerwca 1924 w Dąbrowie Górniczej , zm .
14 czerwca 2006 ) – podoficer , potem oficer aparatu bezpieczeństwa ( w milicji awansował do podpułkownika ) PRL , były komendant obozu w Łambinowicach .
Życiorys .
Do wybuchu II wojny światowej mieszkał w Dąbrowie Górniczej .
Część okupacji spędził w niemieckim obozie w Kochłowicach , z którego uciekł w kwietniu 1943 .
Będąc członkiem Armii Ludowej , ponownie został aresztowany przez Niemców ( 1944 ) , jednak podczas transportu do Auschwitz został odbity przez AL .
Trafił do komunistycznej partyzantki .
Po zakończeniu wojny wstąpił do Milicji Obywatelskiej i szybko awansował na sierżanta .
Do marca 1945 pracował w Świętochłowicach , a następnie w Niemodlinie .
W lipcu 1945 został komendantem Obozu Pracy w Łambinowicach .
Pełnił tę funkcję do października 1945 , kiedy to postawiono mu zarzut `` przekroczenia obowiązków służbowych '' .
Chodziło o spalenie baraku z więźniami i rozkaz strzelania do gaszących go więźniów .
Zginęło wówczas 48 osób .
Mimo tak poważnych zarzutów uczestniczył w procesie żołnierza NSZ Bolesława Pronobisa pseud .
`` Ikar '' ( skazany na śmierć i stracony ) .
W 1947 śledztwo w sprawie wydarzeń w obozie zostało umorzone , a Gęborski został awansowany do stopnia kapitana .
Po 1956 śledztwo w sprawie Łambinowic zostało wznowione , a Czesław Gęborski trafił do aresztu .
Sąd go jednak uniewinnił ( mimo obciążających zeznań najbliższego współpracownika – Ignacego Szypuły ) , a prokuratura nie odwołała się od wyroku .
W 1960 , będąc już oficerem SB , kapitan Gęborski próbował uzyskać odszkodowanie za 22 miesiące aresztu .
Bezskutecznie , sąd uznał areszt za zasadny i potwierdził , że `` wyrok uniewinniający nie rozwiewa wszystkich wątpliwości '' .
Od końca lat 90 .
XX wieku w sądzie w Opolu toczył się wznowiony proces w sprawie śmierci 48 osób .
W wyniku śledztwa w procesie tym prokuratura zdecydowała się postawić Gęborskiemu zarzut zabójstwa podczas tej akcji 35 osób .
Proces ( przerywany notoryczną nieobecnością świadków ) trwał do 2005 , kiedy to został odroczony ze względu na zły stan zdrowia oskarżonego , a ostatecznie umorzony został w 2006 z powodu śmierci oskarżonego .
