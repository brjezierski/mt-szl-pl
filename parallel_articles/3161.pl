Chile
Chile ( , ) , oficjalnie Republika Chile ( , wym .
, ) – państwo w Ameryce Południowej , w południowo-zachodniej części kontynentu .
Zajmuje powierzchnię 756 102 km² , a zamieszkane jest przez 18 549 457 osób ( szac .
2023 ) .
Terytorium Chile rozciąga się wąskim pasem na długości około 4300 km , pomiędzy łańcuchem górskim Andów na wschodzie a wybrzeżem Oceanu Spokojnego na zachodzie .
Znaczna część kraju jest górzysta .
Obejmuje ono także nizinną Dolinę Środkowochilijską , a na północy – pustynię Atakama .
Do Chile należy część archipelagu Ziemi Ognistej , wraz z przylądkiem Horn ( najbardziej na południe wysunięty punkt obu Ameryk ) , a także kilka wysp i archipelagów na otwartych wodach Oceanu Spokojnego , m.in .
Wyspa Wielkanocna i Juan Fernández .
Na wschodzie państwo ma długą granicę lądową z Argentyną , na północy graniczy z Boliwią i Peru .
Położony w regionie aktywnym tektonicznie , kraj okresowo nawiedzany jest przez trzęsienia ziemi ( w tym największe dotychczas odnotowane na Ziemi w 1960 roku ) i tsunami .
Chile jest republiką z systemem prezydenckim .
Stolicą i największym miastem kraju jest Santiago .
Gospodarka w większej części opiera się na sektorze usługowym , choć znaczącą rolę odgrywa także przemysł i górnictwo .
