Czarna Wisełka
Czarna Wisełka – potok źródłowy rzeki Wisły o długości ok. 9 km w Beskidzie Śląskim ze źródłami w postaci wykapów , czyli małych oczek wodnych na mokradłach południowo-zachodniego stoku Baraniej Góry .
Rezerwat pstrąga potokowego .
Dopływami są m.in .
potoki Wolny i Koźli Wierch .
Potok łączy się z Białą Wisełką w Jeziorze Czerniańskim , potok który z niego wypływa nazywa się Wisełką , a po przyjęciu wód Malinki przyjmuje nazwę Wisła .
Wzdłuż Czarnej Wisełki przebiega droga asfaltowa , którą poprowadzony jest czarny szlak turystyczny i ścieżka edukacyjna .
