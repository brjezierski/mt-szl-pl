Czarnoty ( powiat siedlecki )
Czarnoty – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Paprotnia .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Bartłomieja Apostoła w Paprotni .
W latach 1975–1998 miejscowość należała administracyjnie do województwa siedleckiego .
Najstarszym mieszkańcem wsi jest Stanisława Czarnocka .
W miejscowości działa założona w 1968 roku jednostka ochotniczej straży pożarnej .
Od 1997 roku jednostka działa w strukturach Krajowego systemu ratowniczo-gaśniczego .
Straż posiada dwa samochody ratowniczo-gaśnicze : GBA 2/16 Volvo FL6 oraz GBAM 2,5/16+8 Star 244 .
