Czarnowo ( stacja kolejowa )
Czarnowo – dawna stacja kolejowa w Czarnowie , w gminie Zławieś Wielka , w powiecie toruńskim , w województwie kujawsko-pomorskim , w Polsce .
Została otwarta w dniu 28 lutego 1910 roku razem z linią z Torunia Północnego .
W dniu 1 kwietnia 1966 roku linia ta została zamknięta dla ruchu pasażerskiego i towarowego .
