Czuwaszja
Czuwaszja ( , ) , Republika Czuwaska ( czuw .
, ros . )
– republika w europejskiej części Federacji Rosyjskiej .
Geografia .
Położenie i powierzchnia .
Północną jej granicę wyznacza rzeka Wołga .
Na zachodzie graniczy z obwodem niżnonowogrodzkim , na północy z Republiką Mari El , na wschodzie z Republiką Tatarstanu , na południu z Obwodem Uljanowskim , na południowym zachodzie z Republiką Mordwy .
Stolicę – Czeboksary – zamieszkuje ok. 450 tys .
