Czynadijowo
Czynadijowo ( ukr .
Чинадійово ; węg .
Szentmiklós ) – osiedle typu miejskiego na Ukrainie , w obwodzie zakarpackim , w rejonie mukaczewskim .
Leży nad Latoricą w jej górnym biegu , w połowie drogi z Mukaczewa do Swalawy .
Przez miejscowość przebiega droga krajowa M06 ( E50 / E471 ) i linia kolejowa z Czopu do Lwowa ( dwa przystanki kolejowe : Czynadijewo i Czynadijewo II ) .
Historia .
W 1989 liczyła 7289 mieszkańców .
W 2013 liczyła 6908 mieszkańców .
