Czersk ( województwo mazowieckie )
Czersk – wieś w Polsce położona w gminie Góra Kalwaria , w powiecie piaseczyńskim , w województwie mazowieckim .
W średniowieczu Czersk stanowił jeden z najważniejszych ośrodków osadniczych na Mazowszu , znany już od XI wieku .
W wiekach XII i XIII funkcjonował jako gród obronny , potem jako kasztelania i stolica jednego z księstw dzielnicowych .
W latach 1350–1870 miał Czersk prawa miejskie .
Obecnie Czersk znany jest głównie z ruin średniowiecznego zamku książąt mazowieckich , które są główną atrakcją turystyczną tej małej miejscowości i równocześnie jednym z najcenniejszych zabytków na Mazowszu .
Czersk znajduje się w odległości kilku kilometrów od Góry Kalwarii .
Położony jest nad Jeziorem Czerskim , starorzeczem wiślanym .
Kilkaset metrów dalej płynie Wisła .
Przez Czersk z Coniewa do Brzumina prowadzi droga wojewódzka nr 739 , która łączy wieś z drogą krajową nr 79 .
Nazwa .
Najwcześniejszą wzmiankę o nazwie notuje pod rokiem 1142 Latopis Ławrientiewski , jako `` Čьrnьsk '' .
