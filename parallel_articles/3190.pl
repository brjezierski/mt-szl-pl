Czerwiec
Nazwa .
Nazwa miesiąca ( według Brücknera ) pochodzi od słowa `` czerw '' .
Inne źródła wywodzą nazwę miesiąca i nazwę koloru czerwonego od owada czerwca polskiego , z którego wyrabiano czerwony barwnik ( koszenilę ) do tkanin i zbierano w czerwcu na korzeniach rośliny czerwiec trwały .
Nazwa występuje też w innych językach północnej Słowiańszczyzny ( czes .
červen , ukr .
`` червень / czerweń '' , błr .
`` чэрвень / czerwień '' ) .
Inne nazwy używane dawniej to : `` ugornik '' bądź `` zok '' .
Łacińska nazwa `` Iunius '' ( zobacz : kalendarz rzymski ) została zapożyczona przez większość języków europejskich .
Religia .
W Kościele katolickim miesiąc czerwiec poświęcony jest krzewieniu kultu i nabożeństwu ku czci Najświętszego Serca Pana Jezusa .
Wówczas to w kościołach odprawiane są nabożeństwa z wystawieniem Najświętszego Sakramentu i śpiewem Litanii do Serca Jezusowego .
Pogoda .
Pod koniec miesiąca następuje przesilenie letnie na półkuli północnej , a przesilenie zimowe na półkuli południowej .
Pod względem meteorologicznym uznawany w Polsce za miesiąc letni .
