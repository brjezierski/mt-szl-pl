Czerwionka-Leszczyny
Czerwionka-Leszczyny ( śl .
`` Czyrwjůnka-Leszczyny '' ; do 31 grudnia 1991 `` Leszczyny '' ) – miasto w Polsce położone w województwie śląskim , w powiecie rybnickim , siedziba władz gminy miejsko-wiejskiej Czerwionka-Leszczyny .
27 maja 1975 r. do miasta Leszczyny ( prawa miejskie 1962 ) włączono miasto Czerwionka ( prawa miejskie 1962 ) , zachowując tym samym nazwę i ciągłość prawną miasta Leszczyny , a równocześnie likwidując miasto Czerwionka .
1 stycznia 1992 r. nazwę miasta Leszczyny zmieniono na `` Czerwionka-Leszczyny '' .
Z formalnoprawnego punktu widzenia Czerwionka-Leszczyny nie jest więc miastem powstałym przez połączenie miast Czerwionki i Leszczyny , lecz kontynuacją miasta Leszczyny ( do którego włączono Czerwionkę ) , a zmiana nazwy w 1992 r. na `` Czerwionka-Leszczyny '' jest zaledwie uwypukleniem dwoistego charakteru tego miasta a posteriori ( obie części są podobnej wielkości ) .
Według danych z 30 czerwca 2016 roku miasto liczyło 28 283 mieszkańców .
Geografia .
Miasto położone jest na Górnym Śląsku .
Leży między dwiema konurbacjami : rybnicką i katowicką .
Zaliczane jest do aglomeracji rybnickiej .
Miasto leży na Płaskowyżu Rybnickim .
W dzielnicach Czerwionka i Dębieńsko znajduje się góra Ramża ( 325 m n.p.m . ) .
Teren miasta leży w bezpośrednim sąsiedztwie Parku Krajobrazowego `` Cysterskie Kompozycje Krajobrazowe Rud Wielkich '' .
Miasto leży 45 kilometrów od Katowic oraz 40 kilometrów od granicy z Republiką Czeską ( kraj morawsko-śląski ) .
Podział administracyjny .
Od 1999 r. miasto Czerwionka-Leszczyny podzielone jest na cztery dzielnice .
Są to : Czerwionka , Leszczyny , Czuchów i Dębieńsko .
Istnieje także nieformalny podział dzielnic na mniejsze części .
Demografia .
Gmina Czerwionka-Leszczyny liczyła w grudniu 2010 r. 41 144 osób , z czego 28 177 osób mieszkało w mieście , zaś 12 967 osób na obszarach wiejskich .
Liczba ludności gminy ustabilizowała się na poziomie ok. 41 tys .
osób , przy czym liczba ludności miasta nieznacznie maleje , a obszarów wiejskich – rośnie .
W mieście 18,7 % ludności było w wieku przedprodukcyjnym , 65,6 % w wieku produkcyjnym , a 15,7 % mieszkańców było w wieku poprodukcyjnym .
