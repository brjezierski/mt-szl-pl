Czystiakowe
Czystiakowe ( ) – miasto na Ukrainie , w obwodzie donieckim , w Donieckim Zagłębiu Węglowym ; wydobycie węgla kamiennego ; przemysł elektrotechniczny , maszynowy , drzewny , mineralny ; muzeum .
Od 2014 znajduje się pod kontrolą Donieckiej Republiki Ludowej .
17 lipca 2014 roku niedaleko wsi Hrabowe w pobliżu ówczesnego Torezu doszło do katastrofy lotu Malaysia Airlines 17 .
Nazwa .
Od 1964 do 12 maja 2016 miasto nosiło nazwę Torez ( ukr .
Торез ) na cześć francuskiego komunisty Maurice ' a Thoreza .
