Język czarnogórski
Język czarnogórski ( czarnogórski `` crnogorski jezik '' , црногорски jeзик ) – jeden z czterech wariantów standardowych policentrycznego języka serbsko-chorwackiego , oparty , podobnie jak pozostałe warianty serbsko-chorwackiego , na podstawie sztokawskiej .
Jest używany w Czarnogórze .
W przyjętej 19 października 2007 przez parlament tego kraju konstytucji język czarnogórski został uznany za język urzędowy , odrębny od serbskiego , chorwackiego i bośniackiego .
Mimo to spór o status języka czarnogórskiego trwa , gdyż przepisy prawne nie są naukowymi kryteriami służącymi klasyfikacji języków .
Próba stworzenia języka literackiego .
