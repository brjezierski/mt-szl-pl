Kadyks ( prowincja )
Kadyks ( ) – prowincja we wspólnocie autonomicznej Andaluzja w Hiszpanii .
Graniczy z prowincjami : Málaga , Huelva , Sewilla oraz brytyjskim Gibraltarem .
Stolicą prowincji jest miasto Kadyks .
Stolica prowincji , Kadyks , ma 128 000 mieszkańców .
Największe miasto prowincji , Jerez de la Frontera , ma 204 000 mieszkańców .
Innym ważnym miastem jest Algeciras , które ma 114 000 mieszkańców .
