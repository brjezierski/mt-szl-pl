Kordoba ( prowincja )
Kordoba a. Kordowa ( hiszp .
`` Provincia de Córdoba '' ) – prowincja we wspólnocie autonomicznej Andaluzja w Hiszpanii .
Graniczy z prowincjami : Málaga , Badajoz , Ciudad Real , Jaén oraz Grenada .
Stolicą prowincji jest miasto Kordoba .
Kod pocztowy do wszystkich miejscowości na terenie prowincji Kordoba rozpoczyna liczba 14 .
