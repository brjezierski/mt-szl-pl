Cúcuta
Cúcuta – miasto w północno-wschodniej Kolumbii , w pobliżu granicy z Wenezuelą .
Położone jest na wysokości około 320 metrów , na wschodnich stokach Kordyliery Wschodniej ( Andy Północne ) .
Jest ośrodkiem administracyjnym departamentu Norte de Santander .
Według spisu ludności z 30 czerwca 2018 roku miasto liczyło 685 445 mieszkańców .
Charakterystyka miasta .
Miasto zostało założona w 1733 roku pod nazwą San José de Guasimal .
Obecną nazwę przyjęło w 1793 roku .
W Cúcuta rozwinięty jest przemysł odzieżowy , chemiczny , metalowy i ceramiczny .
