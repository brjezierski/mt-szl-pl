D ( wojsko )
D – dzień rozpoczęcia operacji ( działań bojowych ) ; umowny termin , według którego - nie znając rzeczywistej daty rozpoczęcia działań - przeprowadza się kalkulacje wszystkich czynności dowództw i działań wojsk .
Oznaczenie D może być uzupełnione liczbą wyrażającą odpowiednią liczbę dni dzielącą dany dzień od dnia rozpoczęcia operacji , np .
D-3 oznacza trzeci dzień przed rozpoczęciem działań a np .
D+17 - osiemnasty dzień operacji .
