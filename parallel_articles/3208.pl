DOSBox
DOSBox – programowy emulator środowiska DOS-a udostępniany na licencji GNU GPL , ukierunkowany na uruchamianie starych gier komputerowych z platformy PC , które nie mogą działać poprawnie na nowych komputerach .
DOSBox został napisany z użyciem biblioteki SDL 1.2 i został opublikowany na licencji GNU GPL w wersji drugiej .
Program dostępny jest na systemach Microsoft Windows , Linux , macOS , FreeBSD , OS/2 , RISC OS , Solaris 10 i BeOS .
Alternatywne wersje .
Rozwój projektu znacząco zwolnił po wydaniu wersji 0.74 w 2010 roku .
W wyniku tego powstało wiele forków naprawiających błędy , poprawiających kompatybilność z nowymi systemami operacyjnymi , czy dodających nowe funkcje .
Powstało również wiele niezależnych portów na platformy oficjalnie nie wspierane przez DOSBoxa .
