Đà Nẵng
Đà Nẵng − miasto w środkowym Wietnamie , nad Morzem Południowochińskim .
Jedno z pięciu miast wydzielonych na prawach prowincji i największy port morski w tej części kraju .
W okresie kolonialnym miasto nosiło nazwę Tourane .
W 2019 roku trzecie co do wielkości miasto Wietnamu z blisko 1 milionem mieszkańców .
Historia .
Pochodzenie miasta sięga czasów , kiedy dolina rzeki Thu Bồn była nazywana Świętą Ziemią Amarawati i należała do Czamów .
Do XVIII w. miasto nie miało jednak dużego znaczenia zdominowane przez leżący w sąsiedniej odnodze rzecznej port Faifo ( Hội An ) .
Na początku XIX w. port w Đà Nẵng zaczął się rozrastać .
Jednocześnie nastąpiła intensyfikacja działalności katolickich misjonarzy , co sprowokowało niechętny europejczykom dwór cesarski do podjęcia represji .
Niechętny początkowo Indochinom rząd francuski w latach 40 .
XIX w. zmienił politykę i zgodził się na podjęcie interwencji .
