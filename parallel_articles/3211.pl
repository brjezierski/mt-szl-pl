Dekarz
Dekarz – rzemieślnik , zajmujący się pokrywaniem i naprawianiem dachów materiałami dekarskimi tj .
: dachówkami , blachodachówkami , kamieniem np .
łupkiem naturalnym lub strzechą .
Dekarz jako zawód wyodrębnił się w dwóch ostatnich dziesięcioleciach XIX wieku .
Cieśla po wywieszeniu wiechy i poczęstunku u gospodarza pozostawiał dalszą część pracy dekarzowi .
Nazwa pochodzi najprawdopodobniej od deku – formy pokrycia dachowego z targanej słomy , przyciśniętej młodymi drzewkami i ciężkimi gałęziami .
Z tej nazwy wywodzi się słowo dekować , czyli układać dach .
Wcześniej zadaniami dekarza zajmował się cieśla .
Nazwa zawodu pochodzi z niemieckiego `` Decker '' ( osoba pokrywająca ) .
