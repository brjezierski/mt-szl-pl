Łzawnik złocistozarodnikowy
Łzawnik złocistozarodnikowy ( `` Dacrymyces chrysospermus '' Berk .
& amp ; M.A .
Curtis ) – gatunek grzybów z rodziny łzawnikowatych ( `` Dacrymycetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Dacrymyces , Dacrymycetaceae , Dacrymycetales , Incertae sedis , Dacrymycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1873 r. M. J. Berkeley i PW .
Curtis .
Niektóre synonimy :
Nazwę polską nadał Władysław Wojewoda w 2003 .
W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako łzawnik złożonozarodnikowy .
Morfologia .
Owocniki wytwarzane przez ten gatunek są największe spośród wszystkich z tego rodzaju .
Mają szerokość do 5 cm i wysokość do 2 cm .
Początkowo są kuliste , później w miarę wzrostu i rozwoju stają się pofałdowane , głęboko pomarszczone , czasami podobne do małżowiny ucha lub klapowane .
