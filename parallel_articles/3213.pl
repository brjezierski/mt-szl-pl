Łzawnik różnozarodnikowy
Łzawnik różnozarodnikowy ( `` Dacrymyces variisporus '' McNabb ) – gatunek grzybów z rodziny łzawnikowatych ( `` Dacrymycetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Dacrymyces , Dacrymycetaceae , Dacrymycetales , Incertae sedis , Dacrymycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnicy 0,5-4 mm , siedzący , lub wyrastający z podłoża na krótkim trzonku .
Kształt owocnika dyskowaty , soczewkowaty , poduszeczkowaty lub niemal kulisty .
Często owocniki występują grupami , wówczas sąsiednie wskutek nacisku są zdeformowane .
