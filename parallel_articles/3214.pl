Dactylella oxyspora
Dactylella oxyspora ( Sacc .
& amp ; Marchal ) Matsush .
– rodzaj grzybów workowych .
Grzyb saprotroficzny i drapieżny żywiący się nicieniami glebowymi .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Dactylella , Orbiliaceae , Orbiliales , Orbiliomycetidae , Orbiliomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1885 r. Pier Andrea Saccardo i Élie Marchal nadając mu nazwę `` Monacrosporium oxysporum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Takashi Matsushima w 1971 r .
Morfologia i tryb życia .
`` Dactylella oxyspora '' na podłożu PDA tworzy białe kolonie , początkowo bezbarwne , ale szybko tworzące zwarte kępki o nieregularnych krawędziach .
W temperaturze 25ºC po 15 dniach od inkubacji osiągają średnicę 2–2,5 cm .
Strzępki wegetatywne szkliste , septowane , rozgałęzione , przeważnie o szerokości 1-3 μm .
Konidiofory proste , stojące , proste lub lekko zakrzywione , szkliste , czasem sympodialnie rozmnażające się na wierzchołku , tworzące 1–7 konidiów o długości 80–234 μm , szerokości 2–3 μm u podstawy i 2–2,5 u góry .
Konidia szkliste , przeważnie wrzecionowate , czasami maczugowate , proste lub nieco zakrzywione , z 6–11 przegrodami , o wymiarach 50,5– ( 66,5 ) –98,5 × 9– ( 10,5 ) –12 μm .
Wytwarzają się chlamydospory .
Grzyby z rodzaju `` Dactylella '' to grzyby nematofagiczne .
Grzyby te tworzą pętlę z kilku podłużnych komórek .
