Dadaizm
Dadaizm ( dada ) – międzynarodowy ruch artystyczno-literacki w sztuce XX wieku , którego głównymi hasłami były dowolność wyrazu artystycznego , zerwanie z wszelką tradycją i swoboda twórcza odrzucająca istniejące kanony .
Jego członkowie , będący świadkami I wojny światowej , w poczuciu rozpadu cywilizacji negowali powszechnie przyjęte ideały estetyczne i wartości .
Nie wytworzyli jednolitego stylu czy programu , które spójnie łączyłyby ich dzieła – jednoczyła ich raczej wspólna postawa niż styl .
Ruch ukształtował się w Europie i Stanach Zjednoczonych ok. 1915 .
W Zurychu artyści tego nurtu skupieni byli wokół poety Tristana Tzary i malarza Hansa Arpa .
W USA działali m.in .
Marcel Duchamp , Man Ray i Francis Picabia .
Po zakończeniu wojny głównym ośrodkiem ruchu stał się Paryż , ale działały także inne , takie jak : Berlin , Kolonia i Hanower .
Koniec dadaizmu przypadł na początek lat 20 .
XX w .
Dadaiści posługiwali się absurdem , zabawą , dowcipem .
Rzadko wykorzystywali tradycyjne gatunki , takie jak malarstwo , rzeźba i grafika w ich czystej postaci , od których ważniejsza była inwencja .
Charakterystyczne było łączenie różnych technik i tworzenie nowych .
Duchamp w swoich `` ready-made '' do rangi sztuki podnosił zwykłe przedmioty , takie jak pisuar ( `` Fontanna '' , 1917 ) .
Inni artyści tworzyli obrazy abstrakcyjne , kolaże ( przejęte od kubistów ) , fotomontaże i inne dzieła , wymykające się tradycyjnym klasyfikacjom ( np .
`` Podarunek '' Raya – żelazko zaopatrzone w gwoździe ) .
Tworzyli absurdalne teksty i fonetyczne wiersze .
Znaczenie dadaistów polegało na przesunięciu granic sztuki i umożliwieniu artystom swobody wypowiedzi .
Odegrali także rolę w odrzuceniu tradycyjnie pojmowanej sztuki i estetyki , za dzieła uznając przedmioty codziennego użytku , wytwory przypadku czy wręcz śmieci .
Na ruinach dadaizmu wyrósł surrealizm , po części tworzony przez tych samych artystów .
Nazwa .
Słowo `` dada '' jako nazwa ruchu pojawiło się po raz pierwszy w Zurychu , jednak ani jego autorstwo , ani pochodzenie nie są jasne .
Według jednego z przekazów `` dadaizm '' wziął się od francuskiego słowa `` dada '' , które mieli wybrać na chybił trafił ze słownika niemiecko-francuskiego Hugo Ball i Richard Huelsenbeck ( ten drugi napisał później : `` Weźmy słowo dada – powiedziałem – ono jest jakby stworzone dla naszych celów .
Pierwszy dźwięk dziecka wyraża to , co prymitywne , zaczynanie od zera , nowe w naszej sztuce '' ) .
Zgodnie z inną wersją słowo to miał znaleźć Tristan Tzara w słowniku Larousse ' a .
Także znaczenie tego słowa nie jest jednoznaczne – `` dada '' mówią we Francji dzieci na konia , ale także `` dada '' to w języku francuskim synonim słowa `` hobby '' .
Ponadto `` da , da '' to w języku rumuńskim `` tak , tak '' ( także w bułgarskim i rosyjskim )
Według jeszcze innej wersji taka nazwa została przyjęta , ponieważ nic nie oznaczała .
Jak pisał Georges Ribemont-Dessaignes , jeden z dadaistów , nazwa ta `` nie znaczy nic , nie pragnie nic znaczyć i została przyjęta właśnie z powodu owej nicości '' .
