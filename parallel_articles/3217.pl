Daejeon
Daejeon ( ) – miasto na prawach metropolii ( gwangyeoksi ) w środkowej części Korei Południowej , w regionie Hoseo ( dawna prowincja Chungcheong ) , u podnóża gór Noriong , w dolinie rzeki Kum , piąte co do wielkości miasto Republiki Korei .
Położone 167 km od Seulu , 294 km od Pusanu oraz 169 km od Gwangju .
