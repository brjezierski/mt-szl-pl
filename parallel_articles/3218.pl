Dagestan
Dagestan ( , `` Dagestan '' ) , Republika Dagestanu ( ros .
Респу́блика Дагеста́н , `` Riespublika Dagiestan '' ) – autonomiczna , wieloetniczna republika wchodząca w skład Federacji Rosyjskiej .
Nazwa .
Słowo `` Daghistan '' lub `` Daghestan '' ( داغستان ) jest złożeniem tureckiego `` dağ '' ( góra ) i perskiego `` stan '' ( kraj ) .
Geografia .
Republika graniczy z Kałmucją na północy , Krajem Stawropolskim na północnym zachodzie , Czeczenią i Gruzją na zachodzie i Azerbejdżanem na południu .
Leży nad Morzem Kaspijskim .
Stolicą Dagestanu jest Machaczkała .
Strefa czasowa .
Od 2014 r. Dagestan należy do moskiewskiej strefy czasowej ( MSK ) .
UTC +3:00 przez cały rok .
Wcześniej , przed 27 marca 2011 roku , na terenie republiki obowiązywał czas standardowy ( zimowy ) strefy UTC+3:00 , a czas letni – UTC+4:00 .
Demografia .
Dagestan zamieszkuje ok. 3,1 mln mieszkańców ( 2021 ) , tworząc prawdziwą mozaikę etniczną : wyróżnia się tu zwykle 36 autochtonicznych narodowości , z których 12 posiada oficjalny status narodów .
Poza nimi istnieją duże liczebnie społeczności Azerów i Rosjan .
Ludność Dagestanu posługuje się językami należącymi do trzech różnych rodzin językowych .
