Język dagurski
Język dagurski albo daurski – język z rodziny mongolskiej , używany przez ok. 121 tys .
osób w północno-zachodniej Mandżurii oraz w górach Tarbagataj w północno-zachodniej części chińskiego regionu autonomicznego Sinciang ( stan z roku 1990 ) .
Dzieli się na cztery główne dialekty .
Jest językiem ojczystym Dagurów .
