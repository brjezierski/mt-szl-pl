Dakar
Dakar ( wolof : `` Ndakaaru '' ) – stolica i największe miasto Senegalu .
Położone w zachodniej części kraju , nad wybrzeżem Oceanu Atlantyckiego , na Półwyspie Zielonego Przylądka , naprzeciw wysepki Gorée .
Ludność : 2,4 mln ( 2006 ) .
Nazwa .
Nazwa miasta wywodzi się od słowa `` dakhar '' , które w języku wolof oznacza drzewo tamaryndowca .
Gospodarka .
Ze względu na korzystne położenie w Dakarze rozwinął się duży port morski o znaczeniu regionalnym .
Duże znaczenie komunikacyjne ma także port lotniczy Diass .
