Daleki Wschód
Daleki Wschód – nazwa stosowana na określenie obszaru wschodniej Azji , ciągnącego się wzdłuż wybrzeży Pacyfiku .
Najczęściej do tego regionu zalicza się Japonię , Koreę Południową , Koreę Północną , Republikę Chińską ( Tajwan ) oraz wschodnie tereny Chińskiej Republiki Ludowej .
Niekiedy włącza się również Mongolię , kraje Azji Południowo-Wschodniej położone na Archipelagu Malajskim i Półwyspie Indochińskim , wschodnie obszary Rosji oraz rzadko Tybet .
Daleki Wschód jest najludniejszym regionem świata : na obszarze ponad 5 mln km² zamieszkuje tu blisko 2,1 mld osób ( 2012 ) .
