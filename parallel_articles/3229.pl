Dalia Grybauskaitė
Dalia Grybauskaitė ( wym .
[ ] ; ur .
1 marca 1956 w Wilnie ) – litewska polityk i ekonomistka .
Minister finansów ( 2001–2004 ) oraz komisarz ds .
budżetu i programowania finansowego w Komisji Europejskiej ( 2004–2009 ) .
W latach 2009–2019 prezydent Litwy , będąca pierwszą kobietą na tym stanowisku .
Życiorys .
Wykształcenie .
W 1983 ukończyła studia ekonomiczne na Uniwersytecie Leningradzkim , a w 1988 uzyskała stopień kandydata nauk ekonomicznych ( odpowiednik stopnia doktora ) w Akademii Nauk Społecznych przy KC KPZR w Moskwie .
W 1991 ukończyła kurs w ramach programu specjalnego dla kadry kierowniczej w School of Foreign Service na Uniwersytecie Georgetown .
Oprócz języka litewskiego deklaruje również znajomość języków angielskiego , rosyjskiego , polskiego i francuskiego .
Działalność zawodowa .
W wieku 19 lat podjęła pracę w filharmonii państwowej Litewskiej SRR w Wilnie .
W trakcie studiów pracowała w zakładach futrzarskich w Leningradzie .
Po ukończeniu studiów wróciła na Litwę i przez kilka miesięcy pełniła funkcję sekretarza w Litewskiej Akademii Nauk .
W latach 80. była wykładowcą ekonomii politycznej w wyższej szkole partyjnej w Wilnie .
W latach 1990–1991 była kierownikiem wydziału w instytucie badawczym , a w 1991 dyrektorem programowym w biurze Rządu Republiki Litewskiej .
