Dalno
Dalno ( niem .
`` Lindenfelde '' ) – wieś sołecka w Polsce położona w województwie zachodniopomorskim , w powiecie łobeskim , w gminie Łobez .
Wieś jest siedzibą sołectwa Dalno , do którego należą także Trzeszczyna i Przyborze .
Według danych z 29 września 2014 r. wieś miała mieszkańców 450 mieszkających w 29 domach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa szczecińskiego .
W latach 1818-1945 miejscowość administracyjnie należała do Landkreis Regenwalde ( Powiat Resko ) z siedzibą do roku 1860 w Resku , a następnie w Łobzie .
Jest to typowa wieś pofolwarczna , która powstała w 1847 roku jako rezultat komasacji gruntów rolnych będących własnością łobeskiego kupca Borhardta .
Folwark był z początku zarządzany przez gospodarza imieniem Piper , który też stał się pierwszym nabywcą Dalna .
W zachodniej części wsi znajduje się zespół pałacowo-parkowy .
W jego skład wchodzi jednokondygnacyjny eklektyczny pałac z poł .
XIX wieku , z użytkowym poddaszem , przykryty dachem naczółkowym oraz park krajobrazowy powstały w drugiej poł .
XIX wieku , rozciągający się między wąwozami .
We wsi znajduje się świetlica wiejska prowadzona przez Łobeski Dom Kultury , oraz kaplica kościelna należąca do Parafii pw .
Najświętszego Serca Pana Jezusa w Łobzie .
