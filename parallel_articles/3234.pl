Damanhur
Damanhur – miasto w Egipcie , w delcie Nilu , ośrodek muhafazy Al-Buhajra .
Historia .
W starożytności miasto było stolicą VII nomu ( `` A-ment '' ) w Dolnym Egipcie .
Leżało na przy kanale łączącym Mareotis z Canopus lub zachodnią odnogą Nilu .
Było poświęcone bogu Horusowi .
W czasach greckich i rzymskich miasto nazywano Hermopolis Mikra lub Hermopolis Parva , przez odniesienie do Hermesa , identyfikowanego z egipskim Thotem .
Hermopolis było wymieniane przez wielu starożytnych geografów , m.in .
Stefanosa z Bizancjum , Strabona , Ptolemeusza , a także autora `` Itinerarium Antonini '' .
W pierwszych wiekach chrześcijaństwa znajdowała się tu stolica diecezji podległej metropolii w Aleksandrii .
Do tej tradycji odnosi się biskupstwo tytularne w Kościele rzymskokatolickim : `` Hermopolis Parva '' .
