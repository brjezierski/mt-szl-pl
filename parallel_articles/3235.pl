Damaszek ( arab .
ريف دمشق , `` Rif Dimaszk '' ; dawny polski egzonim : `` Damaszek-Okręg '' ) – jedna z 14 jednostek administracyjnych pierwszego rzędu ( muhafaza ) w Syrii .
Jest położona w południowej części kraju .
Graniczy od południa z muhafazami Al-Kunajtira , Dara i Suwajda , od północy z Hims , od wschodu z Jordanią a od zachodu z Libanem .
Muhafaza Damaszek kompletnie otacza miasto wydzielone Damaszek , który jest jednocześnie stolicą muhafazy Miasto Damaszek .
W 2011 muhafaza liczyła 2 836 000 mieszkańców ( druga najliczniejsza po Aleppo ) ; dla porównania , w 2004 było ich 2 273 074 , a w 1981 – 917 364 .
23 czerwca 2003 polska Komisja Standaryzacji Nazw Geograficznych poza Granicami Rzeczypospolitej Polskiej wprowadziła dla tej jednostki egzonim `` Damaszek-Okręg '' , a 23 lutego 2011 zmieniła go na `` Damaszek '' .
