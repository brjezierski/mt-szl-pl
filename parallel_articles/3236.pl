Lokomotywa
Lokomotywa – kolejowy pojazd szynowy z własnym napędem , przeznaczony do ciągnięcia lub pchania innych pojazdów ( wagonów ) po torach .
Charakterystyka .
Lokomotywa w zasadzie nie przewozi ładunków ( choć istnieją lokomotywy zawierające przedział bagażowy ) , zaś w przypadku rozbudowania o pomieszczenia służące do przewozu osób jest to wagon silnikowy .
Lokomotywa może być jedno- , dwu- lub wieloczłonowa .
Czasem nosi nazwę głowicy napędowej ( lokomotywa zespołowa ) , jeżeli stanowi integralną część składu pociągu ( np .
ICE lub TGV ) .
Moc silników zainstalowanych w lokomotywach dochodzą do 25 MW .
Typowa moc lokomotywy ciągnącej pociąg pasażerski to 1–4 MW , zaś jednostki napędowe zainstalowane w składach klasy TGV mają moc ( w zależności od napięcia w sieci trakcyjnej ) od 3,4 do 12,2 MW .
