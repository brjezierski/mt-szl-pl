Dangeau
Dangeau – miejscowość i gmina we Francji , w Regionie Centralnym , w departamencie Eure-et-Loir .
W 2013 roku populacja gminy w ówczesnym rozmiarze wynosiła 958 mieszkańców .
1 stycznia 2018 roku połączono trzy gminy : Bullou , Dangeau oraz Mézières-au-Perche .
Siedzibą gminy została miejscowość Dangeau , od której powzięła nazwę gmina powstała w wyniku połączenia .
