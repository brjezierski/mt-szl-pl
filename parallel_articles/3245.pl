Daniel Jarque
Daniel Jarque González ( ur .
1 stycznia 1983 w Barcelonie , zm .
8 sierpnia 2009 we Florencji ) – piłkarz młodzieżowej reprezentacji Hiszpanii , występujący na pozycji środkowego obrońcy .
Całą zawodową karierę piłkarską spędził w Espanyolu Barcelona .
Życiorys .
W wieku dwunastu lat przeniósł się z amatorskiego klubu CF Cooperativa de Sant Boi de Llobregat do szkółki juniorskiej Espanyolu Barcelona .
W 2001 roku został włączony do kadry drużyny rezerwowej , a 20 września 2002 r. zadebiutował w Primera División w meczu przeciwko Recreativo Huelva .
W 2006 r. jako zawodnik podstawowego składu zdobył z drużyną Puchar Króla i awansował do europejskich pucharów .
W Pucharze UEFA Jarque z Espanyolem dotarli do finału , przegranego jednak po rzutach karnych ( w regulaminowym czasie gry było 2:2 ) z Sevilla FC .
Przed startem nowego sezonu ( 2009/10 ) został wybrany kapitanem drużyny , zastępując Raúla Tamudo .
Nagła śmierć .
8 sierpnia 2009 roku przebywał na zgrupowaniu drużyny Espanyolu we Florencji .
Kiedy rozmawiał przez telefon z narzeczoną , nagle zamilkł i upuścił telefon .
Rozmówczyni domyśliła się , że coś jest nie w porządku i wezwała pomoc .
Okazało się , że doznał ataku serca i pomimo reanimacji zmarł w szpitalu w dzielnicy Florencji Coverciano .
15 sierpnia 2009 roku , kapitan Arsenalu Cesc Fàbregas – kolega z reprezentacji Hiszpanii U-21 w piłce nożnej zadedykował Jarquemu gola zdobytego w 70. minucie podczas meczu z Evertonem wygranego przez Kanonierów 6-1 .
Podniósł on koszulkę Arsenalu z jego nazwiskiem i numerem .
Andrés Iniesta zadedykował Jarquemu zdobytego gola w 116. minucie finału Mistrzostw Świata 2010 przeciwko Holandii pokazując koszulkę z napisem „ Dani Jarque : siempre con nosotros ” ( „ Dani Jarque : Zawsze z nami ” ) .
