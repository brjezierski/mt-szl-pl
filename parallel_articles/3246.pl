Danilo Türk
Danilo Türk ( wym .
; ur .
19 lutego 1952 w Mariborze ) – słoweński polityk , prawnik i dyplomata , prezydent Słowenii od 22 grudnia 2007 do 22 grudnia 2012 .
Życiorys .
Danilo Türk urodził się w 1952 w Mariborze , gdzie uczęszczał również do szkoły podstawowej i średniej .
W dzieciństwie zmarł mu ojciec .
W 1975 ukończył prawo na Uniwersytecie Lublańskim .
Po studiach rozpoczął pracę w jako sekretarz w komisji ds .
