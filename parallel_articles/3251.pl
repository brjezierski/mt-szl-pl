Daphne ( anime )
– seria anime z 2004 roku stworzona przez studio J.C.Staff i GENCO .
W Polsce serial został wydany na dvd przez Anime Gate z polskim lektorem , którym była Danuta Stachyra ( w drugim odcinku specjalnym lektorem był Paweł Straszewski ) .
Anime jest oparte na mandze autorstwa Satoshi Shiki .
Fabuła .
W bliżej nieokreślonej przyszłości woda pokryła znaczną część Ziemi ze względu na skutki globalnego ocieplenia , pozostawiając ludzkości życie w sąsiednich pływających miastach .
