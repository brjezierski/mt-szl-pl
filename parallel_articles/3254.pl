Dariusz Brzozowski
Dariusz „ Daray ” Brzozowski ( ur .
30 stycznia 1980 w Nowym Dworze Mazowieckim ) – polski perkusista rockowy i heavymetalowy .
Członek Akademii Fonograficznej ZPAV .
Współzałożyciel blackmetalowej grupy Vesania i deathmetalowej Masachist .
W latach 2004–2008 członek olsztyńskiej grupy Vader .
Od 2008 roku członek norweskiej formacji Dimmu Borgir , a od 2009 roku polskiej grupy Hunter .
Prowadził również warsztaty perkusyjne .
Brzozowski współpracował ponadto z grupami i wykonawcami jak : Neolithic , Black River , Armagedon , Arysta , Azarath , Pyorrhoea , Sunwheel , Insidious Disease , Nerve , Faust , Crystal Abyss i Crionics , z którą występował podczas koncertów .
Wraz z grupą Vader był dwukrotnie nominowany do nagrody polskiego przemysłu fonograficznego Fryderyka w kategorii album roku metal za wydawnictwa `` The Beast '' ( 2004 ) i `` Impressions in Blood '' ( 2006 ) .
W 2009 roku czasopismo branżowe Topdrummer uhonorowało go tytułem najlepszego perkusisty w kategorii rock metal .
W 2009 roku Daray wziął udział w targach Musikmesse we Frankfurcie , w Niemczech .
Spotkanie odbyło się na stanowisku talerzy perkusyjnych firmy Meinl , która sponsoruje muzyka .
Życiorys .
1994-2004 .
Grę na perkusji Brzozowski rozpoczął w wieku czternastu lat , jednak jego pierwszym instrumentem muzycznym była gitara .
W 1997 roku Brzozowski wraz z gitarzystą Tomaszem `` Orionem '' Wróblewskim i basistą Filipem `` Heinrichem '' Hałuchą założyli grupę muzyczną Vesania .
Na przełomie 2001 i 2002 roku w poszerzonym składzie w olsztyńskim Selani Studio nagrał z grupą debiutancki album pt .
`` Firefrost Arcanum '' .
Płyta ukazała się w 2003 roku nakładem Empire Records w Europie oraz Crash Music w USA .
W 2005 roku ukazał się drugi album grupy Vesania pt .
