Dariusz Dyrda
Dariusz Lucjan Dyrda ( ur .
18 stycznia 1964 w Tychach , zm .
21 lipca 2021 ) – polski pisarz , dziennikarz i samorządowiec związany z Górnym Śląskiem .
Autor powieści political fiction `` Operacja Papież '' ( 2006 ) oraz podręcznika mowy śląskiej `` Rychtig gryfno godka '' ( 2009 ) .
W dorobku ma także kilka książek reportażowych .
Życiorys .
Absolwent dziennikarstwa i politologii Uniwersytetu Śląskiego ( 1989 ) .
Dziennikarz wielu czasopism codziennych ( „ Sztandar Młodych ” , „ Trybuna Robotnicza ” ) , tygodników .
Redaktor naczelny „ Tygodnia w Tychach ” ( 1998 i 2006–2007 ) oraz „ Wiadomości Zagłębia ” ( 2004 ) .
W latach 2010–2011 redaktor naczelny miesięcznika Jaskółka Śląska , będącego organem Ruchu Autonomii Śląska ( RAŚ ) , a od grudnia 2011 redaktor naczelny miesięcznika „ Ślůnski Cajtung ” .
Od 2018 był redaktorem naczelnym tygodnika „ Echo ” z siedzibą w Tychach ( zasięg 6 powiatów ) .
Od czerwca 2020 był redaktorem naczelnym tygodnika `` Tydzień w Tychach , Mysłowicach , Bieruniu , Lędzinach , Mikołowie , Pszczynie Tydzień '' wyd .
przez Megapress z Lędzin .
W 2002 uzyskał mandat radnego Tychów z listy SLD-UP .
W 2006 nie kandydował , zaś w 2010 bez powodzenia ubiegał się o stanowisko burmistrza Lędzin z ramienia komitetu Lędziny21.pl ( zajmując 4. miejsce spośród 7 kandydatów ) oraz o mandat w radzie powiatu bieruńsko-lędzińskiego z listy RAŚ .
W 2011 był kandydatem komitetu Autonomia Dla Ziemi Śląskiej w wyborach do Senatu w okręgu 75 ( przegrał z Elżbietą Bieńkowską , reprezentującą PO ) .
W 2014 ponownie bez powodzenia kandydował do rady powiatu bieruńsko-lędzińskiego .
W 2015 był kandydatem komitetu Ze Śląska – Nie z Partii w wyborach uzupełniających do Senatu , zajmując ostatnie , 3. miejsce .
W latach 2011–2015 członek Rady Naczelnej RAŚ .
Do stycznia 2015 przewodniczący koła bieruńsko-lędzińskiego RAŚ .
W 2017 został jednym z założycieli partii Ślonzoki Razem ( zarejestrowanej w 2018 ) .
W 2019 wystąpił ze wszystkich organizacji .
Syn Augustyna Dyrdy .
Pochowany na cmentarzu komunalnym w Tychach-Wartogłowcu .
