Dariusz Gnatowski
Dariusz Gnatowski ( ur .
24 maja 1961 w Rudzie Śląskiej , zm .
20 października 2020 w Krakowie ) – polski aktor filmowy , teatralny i dubbingowy .
W 1999 zyskał ogólnopolską popularność dzięki roli Arnolda Boczka w serialu `` Świat według Kiepskich '' .
Życiorys .
Wczesne lata .
Wychowywał się w Zabrzu , tam chodził do szkoły podstawowej i liceum .
W 1985 ukończył Państwową Wyższą Szkołę Teatralną im .
Ludwika Solskiego w Krakowie .
Kariera .
Niedługo potem wystąpił jako Pyzo w spektaklu będącym adaptacją powieści `` Ferdydurke '' Witolda Gombrowicza , wyreżyserowanym przez Macieja Wojtyszkę dla Teatru Telewizji .
20 grudnia 1986 zadebiutował w roli Mieczysława Walpurga w dramacie `` Wariat i zakonnica '' Stanisława Ignacego Witkiewicza w reżyserii Krzysztofa Jasińskiego w Teatrze STU w Krakowie .
Z teatrem tym był związany przez całą karierę aktorską .
W latach 1987–1988 oraz 1997–2001 współpracował ponadto z Teatrem Ludowym , znajdującym się w krakowskiej dzielnicy Nowa Huta .
W 1988 po raz pierwszy pojawił się na małym ekranie , występując w pierwszym odcinku serialu `` Crimen '' jako członek sekty kosturowców .
W 1994 miał natomiast miejsce jego debiut na dużym ekranie , kiedy to wcielił się w Barnabę w filmie `` Miasto prywatne '' .
W latach 90 .
