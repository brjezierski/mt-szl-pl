Dariusz Walerjański
Dariusz Walerjański ( ur .
1969 w Zabrzu-Mikulczycach ) – badacz historii Żydów na Górnym Śląsku , obrońca zabrzańskich zabytków .
Obecnie pełni funkcję pełnomocnika ds .
powołania Międzynarodowego Centrum Dokumentacji Zabytków Poprzemysłowych dla Turystyki .
Ukończył historię na Uniwersytecie Śląskim , pracował jako adiunkt w Muzeum Górnictwa Węglowego w Zabrzu , jest założycielem i przewodniczącym Koła Towarzystwa Opieki nad Zabytkami w Zabrzu oraz członkiem Zarządu Oddziału Śląskiego TOnZ w Bytomiu .
Współpracuje z Żydowskim Instytutem Historycznym w Warszawie .
Od lat angażuje się w walkę o uratowanie licznych zabrzańskich zabytków poprzemysłowych : m.in .
wież ciśnień .
Doprowadził też do odnowienia zabrskiego kirkutu , opiekuje się również innymi żydowskimi nekropoliami w regionie .
Uhonorowało go za to Państwo Izrael , które wręczyło mu honorowy dyplom .
W kwietniu 2007 na neonazistowskiej witrynie Redwatch opisano go jako zdrajcę rasy .
Zamieszczono też wykonaną na zabrzanskim kirkucie fotografię Walerjańskiego w otoczeniu uczniów Centrum Kształcenia Zawodowego i Ogólnego w Zabrzu , z którymi kilka miesięcy wcześniej sprzątał cmentarz .
