Darwin ( system operacyjny )
Darwin – uniksowa podstawa systemu operacyjnego macOS firmy Apple Inc. W rzeczy samej jest to pełny system operacyjny , na bazie którego oparto graficzne środowisko użytkownika `` Aqua '' .
Darwin powstał z połączenia mikrojądra Mach-3 oraz narzędzi i usług zaczerpniętych z systemów FreeBSD i NetBSD , mających swe źródła w 4.4BSD .
Tym samym korzenie Darwina sięgają historycznego Uniksa .
Jeden z głównych programistów Darwina , Avie Tevanian , był współtwórcą oryginalnego jądra Mach powstałego na Uniwersytecie Carnegie-Mellon .
Kod Darwina jest wolnodostępny na zasadach Apple Public Source License i rozwijany przez zrzeszoną wokół Apple wspólnotę programistów .
Istnieje także w pełni zgodna wersja zwana GNU-Darwin , rozwijana pod auspicjami projektu GNU .
Obecnie istnieją porty dla architektur PowerPC oraz IA-32 .
Ta pierwsza jest główną i podstawową , Apple rozwija obecnie nową jej wersję dla 64-bitowych procesorów .
