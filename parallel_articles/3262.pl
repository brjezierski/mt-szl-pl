Dasyscyphella nivea ( R .
Hedw . )
Raitv .
– gatunek grzybów z rodziny `` Lachnaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Dasyscyphella , Lachnaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1802 r. Romanus Adolf Hedwig nadając mu nazwę `` Octospora nivea '' .
W 1970 r. Ain Raitviir przeniósł go do rodzaju `` Dasyscyphella '' .
