Dau
Dau ( `` dāwa '' ) – tradycyjny arabski statek żaglowy , z jednym lub kilkoma żaglami .
Pierwotnie używany był u wybrzeży Półwyspu Arabskiego , Indii i wschodniej Afryki .
Duże dau mogą mieć około trzydziestu osób załogi , na mniejszych liczy ona zazwyczaj 12 osób .
Często nazwa jest zapisywana w sposób zaczerpnięty z angielskiego jako `` dhow '' .
Aż do lat 60 .
XX wieku dau odbywały komercyjne rejsy między Zatoką Perską a wschodnim wybrzeżem Afryki używając do poruszania łodzi wyłącznie żagli .
Ładunkiem były głównie daktyle i ryby przewożone do Afryki , a w drodze powrotnej zabierano drewno mangrowe .
Dau kursowały na południe w porze monsunowej zimą i wczesną wiosną , powracały zaś do Arabii późną wiosną lub na początku lata .
Dau obecnie .
Obecnie nazwę „ dau ” noszą też niewielkie , tradycyjnej konstrukcji łodzie używane w celach handlowych na Morzu Czerwonym i w Zatoce Perskiej oraz na akwenie Oceanu Indyjskiego od Madagaskaru po Zatokę Bengalską .
Jednostki takie mają masę między 300 a 500 ton , a ich kształt jest smukły i wydłużony .
Tego typu dau są często używane do przemytu narkotyków oraz piractwa .
Dau jako motyw heraldyczny .
Dau jest częstym motywem heraldycznym w świecie arabskim .
Zostało umieszczone w godle Kuwejtu , Kataru i Zjednoczonych Emiratów Arabskich .
