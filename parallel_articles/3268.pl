Dyneburg
Dyneburg ( ) – miasto na Łotwie , w południowo-wschodniej części kraju , nad Dźwiną ( nazwa w języku łotewskim oznacza zamek nad Daugavą , tj .
Dźwiną ) , miasto wydzielone , drugie co do wielkości miasto Łotwy , stolica i centrum naukowe , przemysłowe i kulturalne Łatgalii .
W 1956 do miasta przyłączono położone na lewym brzegu ( semigalskim ) miasteczko Grīva .
Główny ośrodek Polaków na Łotwie ; większość mieszkańców miasta stanowi ludność rosyjskojęzyczna ( w tym Rosjanie , Białorusini ) oraz Polacy , natomiast Łotysze to ok. 17 % mieszkańców .
Historia .
W państwie zakonnym .
Pierwszy zamek o nazwie Dinaburg zbudował około 1275 Ernst von Rassburg z inflanckiej gałęzi zakonu krzyżackiego .
Warownia w 1277 została zaatakowana przez Litwinów na czele z Trojdenem , którzy próbowali ją bez powodzenia zdobyć .
