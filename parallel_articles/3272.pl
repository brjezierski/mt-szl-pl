David Attenborough
Sir David Frederick Attenborough ( ur .
8 maja 1926 w Londynie ) – brytyjski biolog , popularyzator wiedzy przyrodniczej na świecie , pisarz , narrator i podróżnik .
Młodszy brat aktora i reżysera Richarda Attenborough .
Życiorys .
Ukończył zoologię w Clare College na Uniwersytecie Cambridge i studia podyplomowe w dziedzinie antropologii .
Jako pracownik stacji telewizyjnej BBC , z którą związany jest od 1952 roku , stworzył cykl filmów przyrodniczych , ukazujących nieznane wcześniej fakty z życia zwierząt i roślin .
W 1985 za działalność naukową otrzymał od królowej Elżbiety II tytuł szlachecki .
Został odznaczony Orderem Imperium Brytyjskiego ( CBE ) oraz przyjęty do Royal Society .
W 2005 został odznaczony brytyjskim Orderem Zasługi ( `` Order of Merit '' ) .
Na jego cześć jeden z gatunków ryb pancernych został nazwany `` Materpiscis attenboroughi '' , z kolei gatunek dzbanecznika – `` Nepenthes attenboroughii '' .
W 2012 został uhonorowany Nagrodą Lowella Thomasa .
W 2018 otrzymał nagrodę Emmy w kategorii Najlepszy narrator za dokument `` Błękitna planeta II '' , natomiast w 2019 za `` Naszą planetę '' .
24 września 2020 r. założył konto na Instagramie i w 4 godziny i 44 minuty uzyskał pierwszy milion obserwatorów , a cztery dni później obserwowało go 4,3 mln osób .
Poglądy .
Jest agnostykiem wychowanym w niereligijnej rodzinie naukowców .
W wywiadzie dla Radio Times o jego najnowszym programie o Darwinie i doborze naturalnym przyznał , że dostaje wiele nienawistnych maili od chrześcijan piszących , że „ spłonie w piekle ” .
Jest przeciwnikiem kreacjonizmu , ale nie uważa go za poważne zagrożenie dla nauki .
Za przyczynę obecnego stanu środowiska naturalnego uważa lekceważenie przyrody mające swoje korzenie w `` Księdze Rodzaju '' .
Wzywa , by ochroną objąć znaczną powierzchnie lądów , mórz i oceanów .
Zaangażował się też w popularyzowanie wiedzy o zagrożeniach dla środowiska naturalnego .
Życie prywatne .
W 1950 David Attenborough poślubił Jane Elizabeth Ebsworth Oriel .
Małżeństwo trwało aż do jej śmierci w 1997 .
Para dochowała się dwójki dzieci – Roberta i Susan .
Jego syn Robert jest wykładowcą antropologii fizycznej na Australian National University w Canberze .
