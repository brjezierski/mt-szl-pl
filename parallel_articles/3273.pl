David Bowie
David Bowie , właśc .
David Robert Jones ( ur .
8 stycznia 1947 w Londynie , zm .
10 stycznia 2016 w Nowym Jorku ) – brytyjski wokalista , kompozytor , autor tekstów , multiinstrumentalista , producent muzyczny , aranżer , a także aktor , aktywny w przemyśle muzycznym od 1962 .
Był również malarzem i kolekcjonerem sztuki .
Zaczął występować jako David Bowie , aby nie mylono go z wokalistą zespołu The Monkees o nazwisku Davy Jones .
Prócz tworzenia nagrań solowych , pracował też jako producent artystów , takich jak m.in .
Lou Reed , Iggy Pop czy The Stooges .
Był główną postacią w muzyce popularnej przez ponad cztery dekady i był znany jako innowator , zwłaszcza w latach 70 .
XX wieku .
Zyskał międzynarodową sławę dzięki charakterystycznemu barytonowi , eklektyzmowi pracy , głębi intelektualnej , a także opanowaniu gry na ponad trzynastu instrumentach .
Oprócz swoich umiejętności muzycznych jest uznawany za symbol androgynicznego piękna , który był charakterystycznym elementem jego wizerunku , zwłaszcza w latach 70. i 80 .
XX wieku .
Przeszedł do historii jako jeden z artystów rockowych z największą liczbą sprzedanych albumów w dziejach .
W ciągu pięćdziesięciu dwóch lat kariery nakład ze sprzedaży wszystkich jego płyt wyniósł 140 mln egzemplarzy na całym świecie .
W 1996 uhonorowano go miejscem w Rock and Roll Hall of Fame .
W 2002 został sklasyfikowany na 29. miejscu listy 100 najwybitniejszych Brytyjczyków według BBC .
W 2004 magazyn „ Rolling Stone ” sklasyfikował go na 39. miejscu 100 najwybitniejszych artystów muzycznych wszech czasów .
To samo czasopismo w owym roku umieściło go również na 23. miejscu 100 najlepszych piosenkarzy wszech czasów .
W 2006 znalazł się na 64. miejscu listy 100 najlepszych metalowych wokalistów wszech czasów według „ Hit Parader ” .
