David Butler ( zapaśnik )
David Derek Butler ( ur .
14 września 1957 roku ) – amerykański zapaśnik walczący w stylu klasycznym .
Występował na Igrzyskach Olimpijskich w Seulu 1988 .
Odpadł w eliminacjach turnieju w wadze do 74 kg .
Złoty i srebrny medalista Igrzysk Panamerykańskich .
Srebrny medalista mistrzostw panamerykańskich w 1992 .
Drugie miejsce w Pucharze Świata w 1987 ; trzecie w 1986 i 1988 ; czwarte w 1980 i 1985 i piąte w 1982 .
Dwukrotny medalista wojskowych MŚ w 1983 roku .
