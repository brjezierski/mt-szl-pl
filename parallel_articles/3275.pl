David Duchovny
David William Duchovny ( ur .
7 sierpnia 1960 w Nowym Jorku ) – amerykański aktor filmowy i telewizyjny , reżyser , scenarzysta , producent i wokalista pochodzenia żydowsko-szkockiego .
Znany głównie z seriali `` Z Archiwum X '' i `` Californication '' .
Życiorys .
Wczesne lata .
Jest synem Margaret z domu Miller , szkolnej administratorki i nauczycielki , i Amrama Duchovny ’ ego ( 1927–2003 ) , scenarzysty i publicysty American Jewish Committee .
Miał siostrę Laurie i starszego brata Daniela .
Jego ojciec był Żydem , którego rodzina pochodziła z Ukrainy i Polski , a matka Szkotką ( luterańską imigrantką z Aberdeen ) .
Ojciec usunął z nazwiska literę `` h '' , aby uniknąć pomyłek , które zdarzały się , gdy służył w wojsku .
Uczęszczał do Grace Church School i nowojorskiej szkoły dla chłopców The Collegiate School .
Należał do Phi Beta Kappa Society .
