David Woodard
David Woodard ( wym .
, ur .
6 kwietnia 1964 ) – amerykański pisarz , dyrygent i kompozytor .
W latach dziewięćdziesiątych wymyślił słowo „ prequiem ” , które było składanką słów „ preemptive ” i „ requiem ” .
Prequiem to buddaistyczna praktyka komponowania muzyki , którą gra się podczas lub zaraz przed śmiercią jej podmiotu .
Woodard dyrygował oraz był dyrektorem muzycznym nabożeństw żałobnych w Los Angeles .
W 2001 roku poprowadził ceremonie obywatelską na nieistniejącej już linii kolejowej Angels Flight w pamięci nieszczęśliwego wypadku Leona Praporta i jego rannej wdowy Loli .
Prowadził także prequiemy na łonie przyrody , między innymi w pamięci Brunatnego Pelikana Kalifornijskiego , na plaży , na której zginęło zwierzę .
