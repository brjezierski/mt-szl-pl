Davos ( gsw .
`` Tafaas '' , `` Tafaa '' ; rm .
`` Tavau '' ; wł .
`` Tavate '' ) – miasto i gmina uzdrowiskowa w południowo-wschodniej Szwajcarii , w kantonie Gryzonia , w regionie Prättigau/Davos .
Jest największą gminą w regionie zarówno pod względem liczby mieszkańców jak i powierzchni .
Leży nad rzeką Landwasser .
Położone na wysokości 1 560 m n.p.m .
jest najwyżej położonym miastem w Europie .
