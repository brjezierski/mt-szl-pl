Duńczycy
Duńczycy ( duń .
`` danskere '' ) – naród germański zamieszkujący głównie w Danii ( około 5 mln ) oraz w Niemczech ( około 50 tys. , większość w Szlezwiku ) , USA ( według różnych danych od 320 tysięcy do 1,5 mln osób pochodzenia duńskiego ) , Kanadzie i Szwecji .
Około 90 % Duńczyków należy do kościoła ewangelicko-luterańskiego , ale niewielu z nich praktykuje .
Posługują się językiem duńskim , jednym z języków skandynawskich .
Przodkami Duńczyków byli głównie germańscy Danowie z południowej Skandynawii , którzy opanowali w V–VI wieku Półwysep Jutlandzki .
W VIII wieku rozpoczęli oni łupieżcze wyprawy w Europie zachodniej i u wybrzeży Bałtyku .
W tamtym okresie wielu Duńczyków osiedliło się w Anglii .
W X wieku stworzyli swoje własne państwo , a większość Duńczyków przyjęła chrześcijaństwo .
W następnym stuleciu zaprzestali swoich zbrojnych wypraw .
Na luteranizm przeszli w XVI wieku .
W Polsce nie istnieje oficjalnie żadne skupisko ludności duńskiej , jak również nie stwierdza się oficjalnie istnienia mniejszości duńskiej .
Według niektórych badaczy , właśnie z Danii może się wywodzić polski ród rycerski Awdańców .
Kwestia pochodzenia tego normańskiego rodu nie jest jednak jeszcze do końca wyjaśniona .
