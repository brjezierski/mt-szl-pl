Dania
Dania ( ) – państwo położone w Europie Północnej ( Skandynawia ) , najmniejsze z państw nordyckich .
Wraz z Grenlandią oraz Wyspami Owczymi tworzy Wspólnotowe Królestwo Danii .
Kontynentalna Dania graniczy od południa z Niemcami , zaś przez cieśninę Sund sąsiaduje ze Szwecją .
Posiada też granicę z Kanadą na wyspie Hansa .
Dania jest członkiem Unii Europejskiej , NATO oraz ONZ .
Nazwa państwa .
Nazwa państwa duńskiego pojawiła się po raz pierwszy ok. roku 955 w formie ᛏᛅᚾᛰᛅᚱᚴᛅ , co oznacza „ graniczny las Duńczyków ” ( więcej na ten temat w haśle kamienie runiczne z Jelling ) .
