Ustrój polityczny .
Dania jest monarchią konstytucyjną – głową państwa jest królowa Małgorzata II .
Władzę ustawodawczą sprawuje jednoizbowy parlament noszący nazwę Folketing ( 179 deputowanych ) wybierany na 4-letnią kadencję .
Dania jest najstarszą europejską monarchią z zachowaną ciągłością dziedzicznej władzy monarszej .
Obecnie na tronie duńskim zasiada królowa Małgorzata II , córka Fryderyka IX i królowej Ingrid , z dynastii oldenburskiej , linii Sonderburg-Glücksburg , zamężna z księciem Henrykiem .
Następcą tronu jest starszy syn królowej , książę Fryderyk .
Dania jest monarchią konstytucyjną od 5 czerwca 1849 roku .
Dziedziczenie tronu .
Według Aktu o sukcesji z 4 czerwca 2009 r. tron po zmarłej monarchini obejmuje najstarszy potomek .
Przy braku potomka tron przechodzi na najstarszego brata lub siostrę .
Jeżeli rodzeństwo zmarłego władcy nie żyje lub z innych powodów nie jest upoważnione do objęcia tronu , tron obejmuje najbliższa pokrewieństwem linia boczna panującej dynastii , lecz z ograniczeniem do potomków króla Chrystiana X. Brak możliwości zastosowania tych zasad prowadzi do elekcji nowego króla lub królowej przez parlament .
Na tronie mogą zasiąść tylko spadkobiercy z legalnych związków małżeńskich i jeśli należą do Kościoła Ewangelicko-Luterańskiego .
Związek małżeński króla musi zostać zaakceptowany przez Folketing , a małżeństwa zawarte przez pretendentów do tronu wymagają zgody królowej na forum Rady Państwowej .
Tuż po wstąpieniu na tron monarcha lub monarchini składa przyrzeczenie na piśmie , że będzie przestrzegać postanowień konstytucji .
