Dead Can Dance
Dead Can Dance – brytyjsko-australijska grupa muzyczna prezentująca alternatywne spojrzenie na world music , z wpływami gatunków zaliczanych do szeroko pojętego dark independent .
Powstała w 1981 roku w Melbourne z inicjatywy Brytyjczyka Brendana Perry ’ ego , do którego wkrótce dołączyła Australijka Lisa Gerrard .
Zespół rozwiązano w 1998 roku .
Siedem lat później , w 2005 roku , został on reaktywowany .
Jednak po trasie koncertowej grupa zaprzestała grania .
W 2011 roku duet wznowił działalność .
Historia .
Choć Dead Can Dance uformowali się w 1981 roku w Melbourne , to rok później – zdegustowani wąskimi horyzontami tamtejszej sceny muzycznej – przenieśli się do Londynu .
Tam w ciągu roku podpisali kontrakt z jedną z najsławniejszych wytwórni muzyki alternatywnej – 4AD .
Gdy właściciel firmy , Ivo Watts-Russell , usłyszał nagranie demo grupy i powiedział : „ najsilniejszym wrażeniem było poczucie czegoś wyjątkowo oryginalnego ” , zwrócił także uwagę na „ niezwykły głos Lisy ” .
W 1983 roku Dead Can Dance nagrali sesję dla Johna Peela , a niedługo później światło dzienne ujrzał ich pierwszy album zatytułowany po prostu `` Dead Can Dance '' – kolekcja utworów z lat wcześniejszych .
Nie okazał się on wielkim sukcesem , podobno głównie przez problemy z dźwiękiem .
Dużo większym sukcesem można nazwać ich drugi album `` Spleen and Ideal '' , choćby dlatego , że znalazł się na drugim miejscu brytyjskiej listy przebojów muzyki niezależnej .
Od tego momentu Brendan i Lisa bardziej odważnie eksperymentowali z nowymi instrumentami i formami .
