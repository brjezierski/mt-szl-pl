Dean Koontz
Dean Ray Koontz ( ur .
9 lipca 1945 w Everett ) – pisarz amerykański , autor licznych thrillerów i horrorów .
Zaczął pisać pod koniec lat 60. , skupiając się początkowo na powieściach science fiction .
Później porzucił ten gatunek i częściowo pod prawdziwym nazwiskiem , częściowo pod pseudonimami zajął się pisaniem thrillerów i horrorów .
Przełomowym okazał się rok 1980 , kiedy ukazała się powieść `` Szepty '' ( `` Whispers '' ) .
Kolejne stawały się również bestsellerami .
Obecnie mieszka z żoną Gerdą w Kalifornii .
