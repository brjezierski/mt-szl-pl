Debreczyn
Debreczyn ( , , ) – drugie co do wielkości miasto Węgier , największe miasto we wschodniej części kraju .
W 2011 liczba mieszkańców Debreczyna wynosiła 208,02 tys .
Stolica komitatu Hajdú-Bihar .
Miasto to jest jednym z siedmiu centrów regionalnych kraju oraz największym miastem krainy historycznej Kriszana ( większa jej część położona jest obecnie w Rumunii ) .
Debreczyn dwukrotnie pełnił funkcję stolicy Węgier – w 1849 i 1944 .
Miasto składa się z dwóch części – uzdrowiska i centrum .
Są one połączone linią tramwajową .
Ułatwia ona dostęp z dworca kolejowego do uzdrowiska .
Położenie .
Debreczyn położony jest na Wielkiej Nizinie Węgierskiej , około 30 km od rumuńskiej granicy .
Odległość od Budapesztu drogą lądową wynosi około 224 km , drogą kolejową 221 km ( drogą powietrzną 193 km ) .
