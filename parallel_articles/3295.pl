Debrzno
Debrzno ( tuż po wojnie `` Frydląd Pomorski '' , , ) – miasto w województwie pomorskim , w powiecie człuchowskim , siedziba gminy miejsko-wiejskiej Debrzno .
Według danych z 1 stycznia 2018 Debrzno liczyło 5152 mieszkańców .
Położenie .
Miasto położone jest nad jeziorami : Żuczek oraz Staw Miejski , nad rzeką Debrzynką , w północnej części Krajny , w pasie Pojezierza Południowopomorskiego ( w tym Pojezierza Krajeńskiego ) .
Od północy graniczy z gminami Czarne i Człuchów , od wschodu z gminami Kamień Krajeński i Sępólno Krajeńskie i od południa z Okonkiem i Lipką .
Debrzno znajduje się w odległości 152 km na południowy zachód od Gdańska , oraz 166 km na północ od Poznania .
Przy zachodnich granicach miasta znajduje się florystyczno-stepowy Rezerwat przyrody Miłachowo o powierzchni 3,7 ha .
Historia .
Pierwsze wzmianki dotyczące Debrzna pochodzą z XII w. Osada leżała w granicach średniowiecznej Polski , następnie w XIV w. została zajęta przez Krzyżaków .
W 1354 roku miasto otrzymało prawa miejskie z nadania wielkiego mistrza krzyżackiego Winricha von Kniprode .
Po wybuchu powstania przeciw Krzyżakom , w 1454 miasto uznało władzę króla Polski .
