Tak zakończył się proces formowania pierwszego składu – Mark I , wtedy jeszcze Roundabout .
Wokalistą był Rod Evans , gitarzystą Ritchie Blackmore , basistą Nick Simper , klawiszowcem Jon Lord , a perkusistą Ian Paice .
Nazwa Deep Purple została przyjęta w kwietniu 1968 , a inspiracją był standard muzyczny o tej samej nazwie , uwielbiany przez dziadków Ritchie ’ ego .
Początkowo zespół grał typowy rock głównego nurtu , inspirowany dokonaniami Vanilla Fudge .
W lipcu 1968 roku , wydał swój debiutancki album `` Shades of Deep Purple '' , na którym znalazł się pierwszy przebój grupy `` – Hush . ''
Pierwsze dwa albumy , choć nieźle sprzedające się w USA , pozostały niezauważone w Wielkiej Brytanii .
Evans i Simper po nagraniu trzeciego albumu , `` Deep Purple '' , odeszli z grupy .
Zastąpili ich wcześniej już wspomniany Gillan jako wokalista i Roger Glover jako basista .
Stery w grupie przejmował wtedy Jon Lord , z którego inicjatywy w 1969 zespół spróbował swych sił w repertuarze progresywnym , nagrywając koncert na orkiestrę i grupę rockową .
W lipcu 1969 roku zespół rozpoczął trasę koncertową Deep Purple European Tour .
Zmiany personalne i rozpad zespołu ( 1970–1980 ) .
Prawdziwa sława grupy rozpoczęła się w roku 1970 , kiedy to ostatecznie ukształtowało się jej charakterystyczne brzmienie .
Niektórzy krytycy twierdzą , że cały ruch heavymetalowy wywodzi się od płyty `` Deep Purple in Rock '' wydanej w tym roku .
Była ona oparta na rozbudowanych solówkach gitarowych i organowych , złożonych z melodyjnych riffów , oraz częstych imitacjach , charakterystycznym śpiewie i solidnie grającej sekcji rytmicznej .
Deep Purple znalazło złoty środek pomiędzy prostym brzmieniem ówczesnego hard rocka a wybujałym rockiem progresywnym .
