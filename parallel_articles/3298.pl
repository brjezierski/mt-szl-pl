Czasy najnowsze ( od 2022 ) .
W 2022 zespół opuścił Steve Morse .
Pod koniec marca był zmuszony tymczasowo zrezygnować z koncertowania z powodu problemów zdrowotnych żony , u której zdiagnozowano raka .
Zespół zorganizował zastępstwo w postaci Simona McBride , który już wcześniej współpracował z Ianem Gillanem oraz Donem Aireyem .
23 lipca 2022 Steve Morse wydał oświadczenie o swoim odejściu z zespołu .
Simon McBride został nowym pełnoprawnym gitarzystą grupy .
