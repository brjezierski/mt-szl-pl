Deep Purple
Deep Purple – brytyjski zespół rockowy , założony w Hertfordshire w 1968 ( początkowo pod nazwą `` Roundabout '' ) w następującym składzie : Ritchie Blackmore , Jon Lord , Nick Simper , Ian Paice oraz Rod Evans .
Wspólnie z Led Zeppelin i Black Sabbath jest uważany za pioniera hard rocka i heavy metalu , chociaż niektórzy członkowie zespołu uważają , iż ich muzyki nie da się ściśle zaszufladkować do konkretnego gatunku .
Do swojej twórczości zespół często włączał pierwiastek muzyki poważnej , blues rocka , popu i rocka progresywnego .
Deep Purple został odnotowany w księdze Rekordów Guinnessa , jako „ Najgłośniejszy zespół świata ” .
Jak do tej pory , nakład ze sprzedaży wydawnictw muzycznych formacji sięgnął liczby 100 milionów płyt na całym świecie .
Uznawany za jeden z pierwszych i najbardziej wpływowych zespołów grających hard rock .
W trakcie swojej działalności zespół przeszedł wiele zmian personalnych , zaś w latach 1976–1984 był rozwiązany .
Największy sukces odniósł drugi skład zespołu , zwany Mark II w skład którego wchodzili : Ian Gillan , Ritchie Blackmore , Roger Glover , Jon Lord i Ian Paice .
Ta linia była aktywna od roku 1969 do 1973 , następnie od 1984 do 1989 i ponownie w 1993 , kiedy dalsza współpraca Blackmore ’ a z resztą zespołu wydała się być niemożliwa .
Został on zastąpiony przez Steve ’ a Morse ’ a .
Obecnie skład Deep Purple jest stabilny .
Do roszad doszło jedynie na pozycji klawiszowca , gdy w 2002 roku Jon Lord , który postanowił poświęcić się muzyce poważnej , został zastąpiony przez Dona Aireya .
Jedynym członkiem zespołu występującym w nim nieprzerwanie od jego założenia jest perkusista Ian Paice .
Deep Purple zajął 22. miejsce w rankingu VH1 's `` Greatest Artists of Hard Rock '' .
W 2016 zespół został wprowadzony do Rock and Roll Hall of Fame .
