Reaktywacja i dalsza działalność ( 1984–2021 ) .
W 1984 formacja została reaktywowana i nagrała dwie płyty studyjne – `` Perfect Strangers '' oraz `` The House of Blue Light '' .
W 1989 roku doszło do kolejnego rozłamu w zespole – narastający konflikt pomiędzy Gillanem i Blackmorem spowodował opuszczenie grupy przez wokalistę .
Jego miejsce zajął Joe Lynn Turner , współpracujący wcześniej z Blackmorem w zespole Rainbow .
Skład ten nagrał płytę `` Slaves & amp ; Masters '' .
Album ten , odbiegający od wcześniejszych płyt Deep Purple , jest przez wielu uważany za jedną z najsłabszych płyt zespołu .
Podczas pracy nad kolejnym albumem Joe Lynn został , z inicjatywy Lorda , Glovera i Paice ’ a , zastąpiony przez Gillana , co było sporym zaskoczeniem – Gillan w wywiadach zapowiadał , że jego przygoda z Deep Purple to już przeszłość .
W tym najbardziej znanym składzie , w 1993 roku została nagrana płyta `` The Battle Rages On… '' ( Bitwa nadal wrze ) , której tytuł znakomicie oddawał atmosferę napięcia między gitarzystą a wokalistą zespołu , która panowała już od pierwszego odejścia Iana Gillana z zespołu .
Po tej właśnie płycie odszedł lider Ritchie Blackmore , zastąpiony na czas drugiej części trasy koncertowej przez Joe Satrianiego .
Mimo dobrze układającej się współpracy Joe nie zdecydował się na dołączenie do Deep Purple na stałe .
Stanowisko gitarzysty zespołu , jak się okazało , już na stałe , objął Steve Morse – w 1994 roku został oficjalnie gitarzystą grupy .
W 2002 roku z zespołu odszedł organista Jon Lord , którego zastąpił Don Airey .
