Początki ( 1967–1969 ) .
Powstanie Deep Purple wiąże się z osobą Chrisa Curtisa , członka zespołu The Searchers .
Formowanie zespołu rozpoczęło się na jesieni 1967 roku , wtedy to Curtis zaczął wysyłać telegramy do znajdującego się w Hamburgu Ritchiego Blackmore ’ a .
Mniej więcej rok wcześniej na jednym ze spotkań towarzyskich Curtis poznaje Tony ’ ego Edwardsa , początkującego biznesmana branży muzycznej .
W 1967 odświeża znajomość z Edwardsem , przedstawiając mu propozycję menedżera powstającej właśnie grupy o nazwie Roundabout .
Edwards bardzo przejęty nowym projektem do pomocy dobiera sobie Johna Colettę , szefa agencji reklamowej Castle , Chappel and Partners .
Mając już zapewnioną opiekę menedżera Curtis zabrał się za tworzenie składu grupy .
Pierwszą osobą wybraną do zespołu został współlokator Curtisa , organista Jon Lord .
W międzyczasie dzięki telegramom do Anglii udało się również ściągnąć gitarzystę Ritchiego Blackmore ’ a .
Sam Curtis przewidywał dla siebie rolę wokalisty .
W tym składzie opracowali wersje utworów takich jak „ Strawberry Fields Forever ” The Beatles i „ You Keep Me Hanging On ” The Supremes .
Blackmore dodał też własny utwór instrumentalny „ And the Address ” .
Jednocześnie Chris Curtis zaczął wpadać na coraz dziwniejsze pomysły – chociażby propozycję zatrudnienia w zespole Paula McCartneya , czy granie koncertów na karuzeli – co prawie doprowadziło do zakończenia działalności Roundabout .
Skutkiem tych zachowań było usunięcie Curtisa z Roundabout .
W lutym 1968 zatrudniono w roli basisty Nicka Simpera , znajomego Jona Lorda z The Flowerpot Men .
Jednocześnie rolę perkusisty dostał zarekomendowany przez Blackmore ’ a Bobby Woodman .
W tym momencie kluczową sprawą stała się kwestia wyboru wokalisty .
Jednym z kandydatów był Ian Gillan , który będąc związany z Episode Six , odmówił .
Kolejnym kandydatem był Mick Angus , który polecił zespołowi perkusistę Iana Paice ’ a , będącego jego zdaniem o niebo lepszym od Bobby Woodmana .
Oznaczało to też pożegnanie z tym drugim .
