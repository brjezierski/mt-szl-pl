Deimos ( księżyc )
Deimos ( lub Dejmos , gr .
Deímos ‘ trwoga ’ ) – mniejszy i dalszy z dwóch księżyców Marsa .
Wraz z drugim księżycem Marsa , Fobosem , został odkryty przez amerykańskiego astronoma Asapha Halla .
W mitologii greckiej Dejmos ( jak i Fobos ) był synem Aresa i Afrodyty .
Odkrycie .
Deimos został odkryty przez amerykańskiego astronoma Asapha Halla 12 sierpnia 1877 w obserwatorium marynarki wojennej USA około godziny 7:48 UTC .
Kilka dni później ( 18 sierpnia ) Asaph Hall odkrył również drugi księżyc Marsa , Fobos .
Charakterystyka fizyczna .
Deimos jest prawdopodobnie dawną planetoidą , która w stronę Marsa została skierowana przez pole grawitacyjne Jowisza .
Według jednej z hipotez jego początkowo silnie wydłużona orbita wokółmarsjańska została stopniowo obniżona i zmieniona w niemal kołową w wyniku hamowania aerodynamicznego w atmosferze podczas zbliżeń do planety .
Podobnie jak większość ciał o małych rozmiarach , Deimos jest silnie niesferyczny i ma wymiary 15 × 12 × 10 km .
Deimos składa się ze skał bogatych w węgiel ( podobnie jak planetoidy typu C i chondryty węgliste ) i lodu .
Jego powierzchnia jest pokryta kraterami , ale jednocześnie jest gładsza niż u Fobosa z powodu wypełnienia kraterów grubą warstwą regolitu .
Dwa największe kratery na Deimosie mają średnicę około 3 kilometrów .
Upamiętniają one pisarzy , którzy wspominali o „ dwóch księżycach Marsa ” przed ich odkryciem .
Są to kratery Swift ( od nazwiska Jonathana Swifta ) i Voltaire ( od przydomka Woltera ) .
Orbita .
Mars widziany z Deimosa , ze średnicą kątową 16,7° , jest 1000 razy większy ( powierzchnia ; średnica – 31 razy ) i 400 razy jaśniejszy niż Księżyc w pełni .
Deimos widziany z Marsa ma średnicę kątową nie większą niż 3 ' i gołym okiem jest trudno odróżnialny od gwiazdy .
Jego maksymalna jasność widziana z Marsa jest mniej więcej taka , jak Wenus widzianej z Ziemi .
Ponieważ okres obiegu Deimosa wynoszący 30 godzin 17 minut i 55 sekund prawie pokrywa się z długością marsjańskiej doby ( 24,5 godziny ) , dla obserwatora znajdującego się na równiku między wschodem i zachodem tego księżyca mija aż 2,7 dnia .
Z powodu niewielkiej odległości Deimosa od Marsa i niewielkiego nachylenia jego orbity nie można go obserwować z okolic okołobiegunowych planety ( z szerokości większej niż 82,7° ) .
Zaćmienie Deimosa .
Bardzo częstym zjawiskiem na marsjańskim niebie jest zaćmienie Deimosa przez cień Marsa , ponieważ obiega go w niewielkiej odległości .
W ciągu tamtejszego roku , który trwa prawie 687 ziemskich dni , zdarza się około 110 zaćmień Deimosa .
Zaćmienie to może trwać do czterech godzin .
Ze względu na niewielki rozmiar księżyca są to przeważnie zaćmienia całkowite .
