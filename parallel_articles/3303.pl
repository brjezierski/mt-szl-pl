Głównymi najemcami centrów Dekada są największe sieci handlowe w Polsce : spożywcze , elektroniczne , odzieżowe , obuwnicze , drogeryjne , apteczne i inne oraz sieciowe firmy usługowe i gastronomiczne .
Oferty poszczególnych obiektów są dopasowywane do potrzeb lokalnej społeczności .
Działalność Art & amp ; Business .
Dekada SA angażuje się w projekty kulturalne .
W 2015 roku zainicjowała cykl 10 wydarzeń pod hasłem „ Figury – symbol galerii Dekada w przestrzeni miejskiej ” , w trakcie których przed każdym centrum handlowym Dekada odsłonięto po jednej rzeźbie z cyklu “ Figury ” autorstwa wybitnego współczesnego rzeźbiarza , rektora warszawskiej ASP , prof. Adama Myjaka .
Założeniem projektu było pokazanie , że motorem ożywienia gospodarczego w danej miejscowości jest zarówno nowoczesny handel , jak i kultura .
