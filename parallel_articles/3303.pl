Dekada ( przedsiębiorstwo )
Dekada SA – polska spółka akcyjna , właściciel sieci centrów handlowych Dekada .
W skład obecnego portfela Dekada SA wchodzą obiekty handlowe różnego typu , o powierzchni od kilku do kilkunastu tysięcy mkw. , usytuowane w centrach miast .
