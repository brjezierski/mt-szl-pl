Delatyn ( stacja kolejowa )
Delatyn ( , ) – węzłowa stacja kolejowa w miejscowości Delatyn , w rejonie nadwórniańskim , w obwodzie iwanofrankiwskim , na Ukrainie .
Budynek stacyjny zbudowany w stylu polskim .
Przed II wojną światową uważany za jeden z najpiękniejszych budynków tego typu w Polsce .
Szczególną uwagę zwracała wówczas poczekalnia III klasy urządzona w stylu huculskim .
