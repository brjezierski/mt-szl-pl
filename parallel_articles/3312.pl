Deli Jovan
Deli Jovan ( serb .
Дели Јован ) – góra we wschodniej Serbii , w pobliżu miejscowości Negotin i Bar .
Jej najwyższym punktem jest Crni Vrh o wysokości 1136 m n.p.m .. Na dwóch najwyższych szczytach góry ( Crni Vrh i Veliki Goli Krš o wysokości 1037 m n.p.m . )
znajdują się wieże telewizyjne .
