Delight
Delight – polska grupa muzyczna , powstała w 1997 roku w Skawinie pod nazwą Sator .
Zespół początkowo wykonywał gothic metal z wpływami power metalu .
W latach późniejszych formacja zwróciła się w stronę nowoczesnego rocka z wpływami muzyki elektronicznej .
Od 2007 roku brak jest doniesień o działalności zespołu .
