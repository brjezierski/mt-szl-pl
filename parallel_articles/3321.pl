Denison ( Teksas )
Denison – miasto w Stanach Zjednoczonych , w stanie Teksas , w hrabstwie Grayson .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 miasto liczyło 22 682 mieszkańców , co oznacza spadek o 0,4 % w porównaniu do roku 2000 .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 82,3 % , Afroamerykanie 9,3 % , Azjaci 0,6 % , pozostali 7,8 % .
