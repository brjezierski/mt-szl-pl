Kolcowniczek białowieski
Kolcowniczek białowieski ( `` Dentipratulum bialoviesense '' Domański ) – gatunek grzybów należący do rodziny szyszkogłówkowatych ( `` Auriscalpiaceae '' ) .
Jest jedynym przedstawicielem monotypowego rodzaju Dentipratulum
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Auriscalpium , Auriscalpiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
`` Dentipratulum bialoviesense '' został opisany w 1965 r. przez Stanisława Domańskiego na podstawie okazów zebranych w 1962 r. w Białowieskim Parku Narodowym .
Odnotowano go jedynie na trzech stanowiskach : w Białowieskim Parku Narodowym , we Francji i na Wyspach Kurylskich w Azji .
Drugi raz odnotowano go w Białowieskim PN w 1973 r. Od tego czasu ( do 2010 r. ) nie natrafiono tam na tego grzyba .
Nazwę „ kolcowniczek białowieski ” nadał gatunkowi Władysław Wojewoda w 1997 r. w pracy `` Życie i dzieło profesora Stanisława Domańskiego ( 1916-1993 ) '' .
Stanisław Domański używał nazwy murawka białowieska .
Występowanie .
Gatunek zasiedla martwe drewno drzew liściastych .
Kolcowniczek białowieski ma status wymierającego ( E ) w Czerwonej liście grzybów wielkoowocnikowych Polski .
Gatunki podobne .
Wyglądem i wymaganiami ekologicznymi zbliżony jest do drobnokolca żółknącego `` Mucronella calva '' .
