Depeche Mode
Depeche Mode – brytyjska grupa muzyczna z kręgu muzyki elektronicznej i alternatywnego rocka , która powstała w 1980 w Basildon w Wielkiej Brytanii .
Aktualnie członkami zespołu są Dave Gahan ( wokal ) i Martin Gore ( klawisze , gitara elektryczna i wokal ) .
Obaj należą do składu grupy od początku jej istnienia .
Nazwa grupy powstała na podstawie inspiracji , zasugerował ją Gahan .
Powstanie Depeche Mode poprzedzała grupa Composition of Sound .
W 2006 zespół zdobył statuetkę MTV Europe Music Awards w kategorii „ najlepszy zespół ” .
W 1990 grupa otrzymała nagrodę Rockbjörnen w kategorii „ najlepszy zespół zagraniczny ” .
W 2020 roku Depeche Mode wprowadzono do Rock and Roll Hall of Fame .
Historia .
Wczesne lata ( 1977–1980 ) .
W 1977 roku Vince Clarke ( wokal , gitara ) i Andrew Fletcher ( bas ) założyli zespół No Romance in China , w 1979 Clarke grał na gitarze w `` Plan '' razem z Robertem Marlow oraz Paulem Langwith .
Martin Gore ( gitara ) , który na początku skłaniał się ku punk rockowi , wraz z Philipem Burdettem ( wokal ) grał w `` Norman and the Worms '' , w 1979 Gore , Clarke , Marlow i Paul Redmond utworzyli grupę `` The French Look '' .
W marcu 1980 powstał zespół `` Composition of Sound '' w składzie Clarke ( wokal , gitara ) , Gore ( instrumenty klawiszowe ) , Fletcher ( bas ) .
Aby zarobić na instrumenty – syntezatory , Gore pracował jako urzędnik bankowy , a Fletcher jako agent ubezpieczeniowy .
