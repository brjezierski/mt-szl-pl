Dereźnia-Zagrody
Dereźnia-Zagrody – wieś w Polsce położona w województwie lubelskim , w powiecie biłgorajskim , w gminie Biłgoraj .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 494 mieszkańców i była dziewiątą co do wielkości miejscowością gminy Biłgoraj .
Historia .
Podobnie jak Dereźnia Solska również i Dereźnia-Zagrody dawniej wchodziła w skład miejscowości Dereźnia .
Jako odrębna wieś funkcjonuje od XIX wieku .
Istniejący tutaj od XVIII wieku cech garncarski został odnowiony na początku XX wieku przez przybysza z Nowego Bidaczowa Andrzeja Czyża .
Jego uczeń Jan Kowal przekazał wiedzę o rzemiośle garncarskim innym mieszkańcom wsi .
W 1936 roku na rzece Czarna Łada zbudowano młyn drewniany napędzany turbiną wodną , stojący tam do dzisiaj .
