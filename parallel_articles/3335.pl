Derło
Derło – wieś w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Rokitno .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Apostołów Piotra i Pawła w Pratulinie .
W latach 1945–1975 miejscowość administracyjnie należała do województwa lubelskiego , natomiast następnie w latach 1975–1998 do województwa bialskopodlaskiego .
Historia .
W wieku XIX Derło opisano jako własność rządową na którą składały się wieś i folwark w powiecie konstantynowskim gminie Bohukały , parafii Pratulin .
Najbliższa stacja pocztowa i okręg sądowy w Janowie .
Wieś posiadała 358 mórg gruntów włościańskich domów 37 , ludności 299 .
W folwarku było gruntów mórg 400 .
Dobra Derło składały się z folwarków Derło oraz Kajetanki , Rokitna i obszaru lasu .
W skład dóbr wchodziły wsie Derło , Kajetanka , Rokitno , Lipnica , Michałki i Szurowiec .
Całkowita powierzchnia dóbr według opisu z r. 1855 wynosiła mórg 10828 , w tym las stanowił mórg 2557 .
