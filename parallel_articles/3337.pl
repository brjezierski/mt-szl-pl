Des Plaines
Des Plaines – miasto w hrabstwie Cook , w stanie Illinois , w Stanach Zjednoczonych .
Należy do aglomeracji Chicago .
W roku 2000 Des Plaines zamieszkiwało 58 720 osób .
W tym mieście 15 kwietnia 1955 r. otwarto pierwszą na świecie restaurację McDonald ’ s .
Szkoły .
W Des Plaines znajduje się jeden z kampusów Oakton Community College ( drugi mieści się w Skokie ) .
W mieście znajdują się szkoły podstawowe należące do dystryktów 59 i 62 .
Jest również szkoła średnia , Maine West High School .
