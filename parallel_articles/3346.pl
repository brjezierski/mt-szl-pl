Dezghingea
Dezghingea ( wym .
[ dez'ginʤe̯a ] , gag .
: `` Dezgincä '' , `` Дезгинӂӓ '' , język mołdawski : `` Дезгинӂя '' , ros .
: `` Дезгинжа '' , transliteracja : `` Dezginža '' ) – wieś w autonomicznej Gagauzji ( Mołdawia ) , w dolaju Komrat .
W 94,5 procentach zamieszkana jest przez Gagauzów – turecką grupę etniczną .
We wsi znajduje się stacja kolejowa `` Halta Dezghingea '' , która znajduje się na wysokości 79 m n.p.m .
Posiada ona także rosyjską nazwę `` Станция Дезгинжены '' .
Linia kolejowa prowadzi do Căuşeni .
W Dezghingei ma swą siedzibę szkoła średnia `` Scoala Medie Dezghingea '' .
10 maja 2004 ze środków Mołdawskiego Funduszu Inwestycji Socjalnych pokryto budowę gazociągu doprowadzonego do szkoły we wsi .
Prace nad jego budową zakończono 28 marca 2004 .
Bezrobocie osiąga wartość 80 % .
Dezghingea była jedną z mołdawskich miejscowości , które pod koniec 2007 roku zostały objęte programem `` Proiectul Dezvoltare Comunitara '' wspomagającym finansowo wioski dotknięte suszą , która nawiedziła tereny Mołdawii latem 2007 .
Kampania była współfinansowana ze środków Szwedzkiej Agencji Rozwoju oraz fundacji Soros-Moldova .
Pomoc polegała na doprowadzeniu wodociągów zapewniających bieżącą wodę mieszkańcom wsi .
Zaraz po kataklizmie wieś odwiedzili australijscy sanitariusze z `` Mobile Medical Team '' .
Jako że najbliższy szpital oddalony jest od Dezghingei o 10 km , zapewniali oni podstawową pomoc medyczną .
W ramach samopomocy mieszkańcy Dezghingei wzięli udział w programie finansowanym przez Program Narodów Zjednoczonych ds .
Rozwoju w Mołdawii ( także pod ich patronatem odbywała się kampania `` Proiectul Dezvoltare Comunitara '' ) .
Z pozyskanych środków posadzono 1500 drzew , wyczyszczono i odnowiono trzy kilometry drogi oraz dwa kilometry nadbrzeża rzeki Cişmea .
Od 2002 do 2006 roku w okolicach wsi Dezghingea prowadzono działania na rzecz ochrony naturalnej roślinności stepowej .
Przedsięwzięcie finansowane było z budżetu kraju oraz źródeł międzynarodowych , a odpowiadało za nie mołdawskie Ministerstwo Środowiska .
Inne nazwy miejscowości to `` Desghinge '' , `` Deshinge '' , `` Dezghinge '' , `` Dezgindzhe '' , `` Dezginzha '' , `` Dezginzhe '' .
