Phomopsis helianthi
Phomopsis helianthi Munt.-Cvetk. , Mihaljč .
& amp ; M. Petrov – gatunek grzybów z rzędu `` Diaporthales '' .
U słoneczników ( `` Helianthus '' ) wywołuje grzybową chorobę o nazwie rak łodyg słonecznika .
Systematyka .
`` Phomopsis , Diaporthaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungii '' .
Po raz pierwszy gatunek ten opisali Maria Muntañola-Cvetković , M. Mihaljčević i Marija Petrov w 1981 r .
Synonim : `` Diaporthe helianthi '' Munt.-Cvetk. , Mihaljč .
& amp ; M. Petrov 1981 .
Morfologia i rozwój .
Grzyb mikroskopijny , saprotrof i pasożyt roślin .
Tworzy zarówno stadium bezpłciowe ( anamorfę ) , jak i płciowe ( teleomorfę ) .
Jeszcze do niedawna teleomorfę zaliczano do odrębnego gatunku `` Diaporthe helianthi '' , obecnie nazwa ta jest synonimem `` Phomopsis helianthi '' .
Zimują pyknidia na resztkach roślin w ziemi .
Mają kształt kulisty lub prawie kulisty , barwę od żółtej do czarnej , średnicę 400 µm i wystające długie szyjki o długości od 350 do 600 µm .
