Diaporthe perniciosa
Diaporthe perniciosa Marchal & amp ; É.J .
Marchal – gatunek grzybów z rzędu `` Diaporthales '' .
U drzew owocowych wywołuje grzybową chorobę o nazwie rak kory drzew owocowych .
Systematyka .
Po raz pierwszy gatunek ten opisali w Belgii Élie Marchal i Émile Jules Joseph Marchal w 1921 r. na owocach .
Charakterystyka .
Występuje na jabłoniach ( `` Malus '' ) , gruszach ( `` Pyrus '' ) i śliwach ( `` Prunus '' ) .
Dominuje anamorfa .
Tworzy ona dwa rodzaje zarodników konidialnych :
Teleomorfa jest saprotrofem , powstaje rzadko , tylko na martwych gałęziach .
Tworzy w skupiskach kuliste lub butelkowate perytecja o wydłużonej szyjce .
Powstają w nich dwukomórkowe , szkliste askospory o wymiarach 10–17 × 2,5–5 µm .
Źródłem infekcji pierwotnej są zarówno konidia , jak i askospory .
Infekują rośliny przez różnego rodzaju zranienia .
