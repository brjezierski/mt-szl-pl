Diatrype decorticata ( Pers . )
Rappaz – gatunek grzybów z rodziny `` Diatrypaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Diatrypaceae , Xylariales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Christiaan Hendrik Persoon , nadając mu nazwę `` Sphaeria stigma '' var .
`` decorticata '' Pers .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1987 François Rappaz .
