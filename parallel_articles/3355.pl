Didymella applanata
Didymella applanata ( Niessl ) Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Saprotrof i pasożyt roślin .
U malin wywołuje chorobę o nazwie przypąkowe zamieranie pędów maliny .
Występuje we wszystkich rejonach świata , w których uprawia się maliny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Didymella , Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1875 r. Gustav Niessl von Mayendorf , nadając mu nazwę `` Didymosphaeria applanata '' .
