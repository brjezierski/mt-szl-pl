Didymella bryoniae
Didymella bryoniae ( Fuckel ) Rehm – gatunek grzybów z klasy `` Dothideomycetes '' .
Saprotrof i pasożyt roślin z rodziny dyniowatych ( `` Cucurbitaceae '' ) .
Wywołuje u nich chorobę o nazwie czarna zgnilizna zawiązków i pędów roślin dyniowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1870 r. K.W.G .
Fuckel nadając mu nazwę `` Sphaeria bryoniae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu H. Rehm w 1881 r .
Synonimy :
Morfologia i rozwój .
Wytwarza dwa rodzaje owocników : pseudotecja i pyknidia .
