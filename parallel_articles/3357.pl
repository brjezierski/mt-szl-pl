Didymella fabae
Didymella fabae G.J .
Jellis & amp ; Punith – gatunek grzybów z klasy `` Dothideomycetes '' .
Wywołuje chorobę o nazwie askochytoza bobiku .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
W wielu opracowaniach gatunek ten znany jest pod nazwą `` Ascochyta fabae '' .
Speg .
Obecnie wiadomo , że takson ten opisany jako odrębny gatunek , w rzeczywistości jest tylko anamorfą `` Didymella fabae '' ( forma rozmnażająca się bezpłciowo ) .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Strzępki bezbarwne lub żółtawe , obficie rozgałęzione , z przegrodami .
Pyknidia częściowo zanurzone , żółtobrązowe , niemal kuliste , z jedną ostiolą .
Mają rozmiar 200–250 μm .
Komórki wewnętrzne bezbarwne , o kształcie od cylindrycznego do niemal kulistego .
Pykniospory bezbarwne , zakrzywione , o podstawie nieco zaokrąglonej .
Mają 1-3 przegrody i rozmiar 16–24 × 3,5–6 μm .
Worki ułożone w rzędach , zanurzone , ciemnobrązowe , o rozmiarach 55–70 × 10–14 μm .
Askospory bezbarwne , gładkie , szeroko elipsoidalne , dwukomórkowe , zwężone w miejscu przegrody , przy czym górna część jest szersza od dolnej .
Mają rozmiar 15–18 × 5,5–6,5 μm .
