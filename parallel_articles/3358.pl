Didymella pisi
Didymella pisi Chilvers , J.D .
Rogers & amp ; Peever – gatunek grzybów z klasy `` Dothideomycetes '' .
Saprotrof i pasożyt .
U grochu jest jednym z patogenów wywołujących chorobę o nazwie zgorzelowa plamistość grochu .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Didymella , Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Holotyp : WSP 71448 .
W warunkach naturalnych obserwowano tylko bezpłciową anamorfę `` Ascochyta pisi '' .
W laboratorium udało się jednak zaobserwować teleomorfę wytwarzająca płciowo askospory .
Zaklasyfikowano ją do rodzaju `` Didymella '' '' .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Anamorfa wytwarza pyknidia o średnicy 100–250 μm i barwie od żółtobrązowej do ciemnobrązowej .
