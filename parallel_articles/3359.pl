W wieku 10 lat zapisał się do młodzieżowej drużyny piłkarskiej Los Cebollitas .
W profesjonalnym futbolu zadebiutował w 1976 , dołączając do Argentinos Juniors .
Dla Argentinos zagrał w 167 spotkaniach , strzelając 115 bramek .
Lata 1981–1982 spędził w CA Boca Juniors , gdzie rozegrał 40 meczów i strzelił 28 goli .
W 1979 , 1980 i 1981 był królem strzelców Primera División , zdobywając kolejno 27 , 25 i 40 bramek .
