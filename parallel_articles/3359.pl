Diego Maradona
Diego Armando Maradona ( ur .
30 października 1960 w Lanús , zm .
25 listopada 2020 w Tigre ) – argentyński piłkarz i trener piłkarski .
Nazywany także „ El Diez ” ( „ Dziesiątka ” ) i „ Pelusa ” ( „ Puszek ” ) .
Uważany za jednego z najlepszych graczy w historii tego sportu , został wybrany najlepszym zawodnikiem XX wieku ( wraz z Pelém ) , uzyskując 53 % głosów w sondzie na oficjalnej stronie FIFA .
Zdobył trzecie miejsce w głosowaniu zrealizowanym przez członków `` Komisji Futbolowej '' FIFA i ludzi prenumerujących `` FIFA Magazine '' .
Przez większość swojego życia Maradona zdobywał wielkie trofea zarówno z reprezentacją Argentyny , jak również z niektórymi klubami , w których grał .
Z reprezentacją zdobył mistrzostwo świata w 1986 , wicemistrzostwo na mundialu w 1990 i mistrzostwo na mistrzostwach świata juniorów w 1979 .
Najważniejsze trofea na poziomie klubowym zdobył grając w SSC Napoli , z którym zdobył Puchar UEFA oraz jedyne dwa tytuły `` mistrza Włoch '' w historii drużyny .
Po zakończeniu kariery sportowej związał się z telewizją , prowadził program `` La Noche del 10 '' , emitowany przez Canal 13 pod koniec 2005 roku .
Był także wiceprezesem `` Comisión de Fútbol '' Atlético Boca Juniors od czerwca 2005 do sierpnia 2006 roku .
Od 2008 do 2010 był selekcjonerem seniorskiej reprezentacji Argentyny , z którą wywalczył awans do Mistrzostw Świata 2010 w RPA , a w turnieju finałowym awansował do ćwierćfinału .
