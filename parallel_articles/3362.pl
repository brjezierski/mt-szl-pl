Dimeromyces balazucii
Dimeromyces balazucii W. Rossi & amp ; Cesari – gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisali W. Rossi & amp ; Cesari w 1977 r. na owadzie `` Scaphidema metallicum '' we Włoszech .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski opisał jego występowanie na chrząszczu `` Scaphidema metallicum '' z rodziny czarnuchowatych ( Tenebrionidae ) .
