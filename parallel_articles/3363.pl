Dimitri Payet
Dimitri Payet ( ur .
29 marca 1987 w Saint-Pierre ) – francuski piłkarz występujący na pozycji pomocnika we francuskim klubie Olympique Marsylia .
Kariera klubowa .
Payet karierę rozpoczynał jako junior w klubie JS Saint-Pierroise z Reunionu .
W 1999 roku trafił do juniorskiej ekipy Le Havre AC .
Spędził w niej 4 lata , a potem powrócił do ojczyzny .
Przez kolejne dwa lata reprezentował tam barwy klubu SS Excelsior .
W 2005 roku podpisał kontrakt z francuskim FC Nantes , grającym w Ligue 1 .
W tych rozgrywkach zadebiutował 17 grudnia 2005 w zremisowanym 0:0 pojedynku z Girondins Bordeaux .
21 stycznia 2006 w wygranym 4:1 pojedynku z FC Metz strzelił pierwszego gola w trakcie gry w Ligue 1 .
W 2007 roku spadł z klubem do Ligue 2 .
Wówczas odszedł z Nantes .
W lipcu 2007 za 4 miliony euro trafił do pierwszoligowego AS Saint-Étienne .
Pierwszy ligowy mecz zaliczył tam 4 sierpnia 2007 przeciwko AS Monaco ( 1:1 ) .1 lipca podpisał kontrakt z francuskim Olympique Marsylia .
Latem 2015 roku podpisał kontrakt z West Hamem .
29 stycznia 2017 powrócił do Olympique Marsylia za kwotę 29,3 milionów euro .
Kariera reprezentacyjna .
Payet był reprezentantem Francji U-21 .
W kadrze młodzieżowej zagrał 12 razy i zdobył 4 bramki .
9 października 2010 zadebiutował w dorosłej reprezentacji w wygranym 2:0 meczu eliminacji do Euro 2012 z Rumunią .
Wystąpił także kilka dni później w meczu przeciw reprezentacji Luksemburga .
W obu tych meczach zaliczył po asyście przy golach Yoanna Gourcuffa .
Strzelił gola ustalając tym samym rezultat na 2:1 w 89. minucie meczu otwarcia Euro 2016 rozegranego 10 czerwca 2016 pomiędzy Francją a Rumunią .
