Dimmu Borgir
Dimmu Borgir ( IPA [ ] ) – norweska grupa muzyczna wykonująca black metal , powstała w 1993 roku w Oslo .
Do 2010 roku grupa wydała osiem albumów studyjnych oraz szereg kompilacji i minialbumów , które zostały pozytywnie ocenione zarówno przez fanów , jak i krytyków muzycznych .
Zespół stanowią Stian „ Shagrath ” Thoresen , Sven „ Silenoz ” Atle Kopperud i Thomas Rune „ Galder ” Andersen Orre .
Zespół dał szereg koncertów na całym świecie i uczestniczył w licznych festiwalach m.in .
takich jak : Wacken Open Air , Gods of Metal i Ozzfest .
Został wielokrotnie nagrodzony i wyróżniony w plebiscytach muzyki heavy metalowej oraz określony m.in .
jako „ najpopularniejsza grupa black metalowa na świecie ” .
W twórczości zespół nawiązał do takich zagadnień jak mizantropia , satanizm i antychrześcijaństwo .
Muzyka Dimmu Borgir to melodyjny black metal z wiodącymi partiami instrumentów klawiszowych .
Od momentu wydania albumu `` Puritanical Euphoric Misanthropia '' w 2001 roku , zespołowi podczas nagrań towarzyszy orkiestra symfoniczna .
Z tego powodu został również zaliczony do nurtu symfonicznego black metalu .
Podczas koncertów Dimmu Borgir prezentuje charakterystyczny rodzaj ekspresji , obejmujący corpse paint oraz stosuje rozwiązania scenograficzne z użyciem efektów pirotechnicznych .
Historia .
1993–1999 .
Zespół powstał w 1993 roku z inicjatywy multiinstrumentalisty Stiana „ Shagratha ” Thoresena , gitarzysty Svena „ Silenoza ” Atle Kopperuda , basisty Brynjarda Tristana , perkusisty Kennetha „ Tjodalva ” Åkessona oraz klawiszowca Stiana „ Nagasha ” Aarstada .
Nazwa grupy została zaczerpnięta od wulkanicznego wzgórza Dimmuborgir w Islandii .
Rok później nakładem Necromantic Gallery Productions ukazał się debiutancki minialbum zatytułowany `` Inn I Evighetens Mørke '' , wydany w limitowanym do 1000 egzemplarzy nakładzie .
Niedługo później zespół podpisał kontrakt z wytwórnią No Colours , dzięki której pojawił się na rynku debiutancki album pt .
`` For All Tid '' .
Dwa lata później ukazał się drugi album pt .
`` Stormblåst '' .
Przed jego nagraniem Shagrath objął funkcję gitarzysty w zespole , zaś za perkusją zasiadł Tjodalv .
W tym samym roku został zarejestrowany minialbum pt .
`` Devil ’ s Path '' .
Wkrótce potem dotychczasowy basista został usunięty z zespołu , a jego miejsce zajął Nagash .
Nieobecność Aarstada , spowodowana służbą wojskową , skłoniła Shagratha do osobistego nagrania partii instrumentów klawiszowych na minialbumie .
W 1997 roku zespół podpisał kontrakt z niemiecką wytwórnią Nuclear Blast .
W maju tego samego roku ukazał się album pt .
`` Enthrone Darkness Triumphant '' .
