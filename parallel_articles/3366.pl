Dino Merlin
Dino Merlin , właśc .
`` Edin Dervišhalidović '' ( ur .
12 września 1962 w Sarajewie ) – jugosłowiański i bośniacki kompozytor i piosenkarz i autor tekstów , wokalista zespołu `` Merlin '' w latach 1983–1991 .
Dwukrotny reprezentant Bośni i Hercegowiny w Konkursie Piosenki Eurowizji ( w 1999 i 2011 ) .
Życiorys .
Wczesne lata .
Uczęszczał do szkoły podstawowej „ Moris Moco Salom ” , która w późniejszych latach będzie inspiracją dla jego twórczości .
Następnie ukończył Techniczną Szkołę Średnią w Sarajewie .
Podjął studia , jednak przerwał je po pierwszym semestrze , chcąc skupić się na działalności muzycznej .
By się utrzymać , dorabiał jako pracownik fabryki metalu na przedmieściach stolicy .
Gdy miał 12 lat , otrzymał od matki gitarę , na której samodzielnie nauczył się grać .
Niedługo później zaczął pisać swoje pierwsze melodie i teksty piosenek .
Pierwszy utwór skomponował , mając 14 lat .
Jako nastolatek brał udział w kilku konkursach muzycznych , w których wykonywał własne interpretacje piosenek znanych wykonawców .
Kariera .
Współpraca z zespołem Merlin .
W czasach licealnych zaczął grać pierwsze koncerty wraz z przyjacielem z dzieciństwa , klawiszowcem Mirsadem Lutvicą , który pomagał mu w nauce gry na gitarze , a kilka lat później wraz z nim założył zespół Merlin .
Przyjaźnił się również z młodszym bratem Lutvicy , Mensurem , który towarzyszył mu , gdy grali koncerty na ulicach Sarajewa .
W 1983 wraz z przyjaciółmi założył zespół Merlin , w którym odpowiedzialny był za pisanie tekstów , komponowanie muzyki oraz śpiewanie .
Kilka miesięcy później za własne pieniądze wynajął studio muzyczne należące do Brano Likicia , by zarejestrować napisane przez siebie piosenki .
Nagranie nie zostały przyjęte przez żadną z czołowych wytwórni muzycznych w Sarajewie .
W trakcie nagrywania jednej z piosenek został dostrzeżony przez odkrywcę talentów Muradifa Brkicia , który zaproponował im podpisanie kontraktu ze swoją wytwórnią muzyczną Sarajevodisk .
Efektem współpracy był pierwszy album studyjny zespołu , zatytułowany `` Kokuzna vremena '' z 1985 .
W następnych latach wydali jeszcze cztery albumy : `` Teško meni sa tobom , a još teže bez tebe '' ( 1986 ) , `` Merlin '' ( 1987 ) , `` Nešto lijepo treba da se desi '' ( 1989 ) i `` Peta strana svijeta '' ( 1990 ) .
Niedługo po premierze piątej płyty zakończył współpracę z muzykami , chcąc skupić się na karierze solowej .
Kariera solowa .
W 1991 przybrał pseudonim Dino Merlin , a w swych utworach zaczął łączyć orientalne instrumenty wraz z nowoczesnym brzmieniem muzyki elektronicznej .
W 1993 wydał pierwszy , solowy album studyjny , zatytułowany `` Moja bogda sna '' .
W 1995 zaprezentował drugą płytę pt .
`` Fotografija '' , a także napisał tekst państwowego hymnu „ Jedna si jedina ” .
W 1997 nagrał singiel „ Ljubav je ” w towarzystwie kilku innych lokalnych artystów , takich jak m.in .
Srđan Jevđević , Hajrudin Varešanović czy Dražen Žerić .
W 1999 wydał piosenkę „ Putnici ” , którą nagrał w duecie z Béatrice Poulot .
Zaprezentowali ją podczas koncertu `` BH Eurosong '' , wyłaniającego reprezentanta Bośni i Hercegowiny w 44 .
Konkursie Piosenki Eurowizji , i zajęli drugie miejsce , przegrywając jedynie z utworem „ Starac i more ” zespołu Hari Mata Hari .
Po dyskwalifikacji grupy zostali ogłoszeni nowymi przedstawicielami kraju w konkursie organizowanym w Jerozolimie .
W finale konkursu zajęli siódme miejsce , zdobywszy 86 punktów .
Również w 1999 wydał swój pierwszy album koncertowy pt .
`` Vječna vatra '' , zawierającym zapis audiowizualny koncertu , który zagrał 23 lipca 1998 w Sarajewie .
W 2000 wydał trzeci solowy album studyjny pt .
`` Sredinom '' , a w maju 2004 – płytę pt .
`` Burek '' , która sprzedała się w ponad 600 tys .
egzemplarzach .
W lipcu zaśpiewał gościnnie podczas ceremonii otwarcia Starego Mostu , odnowionego przez UNESCO po jedenastu latach od zniszczeniu w 1993 .
W 2005 wydał drugi album koncertowy pt .
`` Live Koševo 2004 '' , zawierający zapis audiowizualny koncertu zagranego pod koniec lipca 2004 na stadionie Koševo w Sarajewie .
W trakcie widowiska gościnnie wystąpiło kilku innych piosenkarzy z krajów byłej Jugosławii , w tym np .
Nina Badrić , Ivana Banfić i Željko Joksimović .
Piosenka „ Superman ” , którą nagrał z Joksimoviciem , dotarła do pierwszego miejsca krajowych list przebojów .
W połowie 2007 wydał singiel „ Otkrit ću ti tajnu ” , którym zapowiadał swój kolejny album pt .
`` Ispočetka '' , wydany w czerwcu 2008 nakładem chorwackiej wytwórni City Records .
Drugim singlem z płyty była piosenka „ Dabogda ” , którą nagrał z Hari Varešanoviciem , wokalistą zespołu Hari Mata Hari .
W 2011 z piosenką „ Love in Rewind ” reprezentował Bośnię i Hercegowinę w 56 .
Konkursie Piosenki Eurowizji .
14 maja otworzył stawkę drugiego koncertu półfinałowego i z piątego miejsca awansował do finału rozgrywanego 16 maja .
Zajął w nim szóste miejsce po zdobyciu łącznie 125 punktów , w tym m.in .
maksymalnych not 12 punktów od Austrii , Macedonii , Serbii , Słowenii i Szwajcarii .
