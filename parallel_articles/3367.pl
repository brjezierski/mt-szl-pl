Dioicomyces anthici
Dioicomyces anthici Thaxt.– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Dioicomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1901 r. Roland Thaxter na owadzie `` Anthicus californicus '' w USA .
Jest gatunkiem typowym rodzaju `` Dioicomyces '' .
Synonim : `` Dioicomyces anthici '' var .
`` fuscescens '' Maire 1920 .
Charakterystyka .
Grzyb mikroskopijny , pasożyt owadów , nie powodujący jednak jego śmierci i zwykle nie wyrządzający mu większych szkód .
W Polsce jego występowanie podał Tomasz Majewski na chrząszczu `` Anthicus flavipes '' z rodziny nakwiatkowatych ( Anthicidae ) .
