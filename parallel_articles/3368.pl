Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Diplocarpon , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
W Polsce u truskawki wywołuje chorobę o nazwie czerwona plamistość liści truskawki .
Po raz pierwszy takson ten w 1884 r. opisali Job Bicknell Ellis i Benjamin Matlack Everhart nadając mu nazwę `` Peziza earliana '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Frederick Adolph Wolf w 1924 r. ( jako `` Diplocarpon earliana '' ) .
Ma około 20 synonimów .
Cykl życiowy .
`` Diplocarpon aerlianum '' zimuje w porażonych liściach .
Może rozwijać się przez cały rok , nawet pod pokrywą śnieżną , ale niska temperatura znacznie zmniejsza tempo jego rozwoju .
W porażonych liściach tworzy służące do rozmnażania się acerwulusy .
