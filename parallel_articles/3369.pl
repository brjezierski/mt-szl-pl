Diplocarpon mespili
Diplocarpon mespili ( Sorauer ) B. Sutton – gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Grzyb mikroskopijny rozprzestrzeniony na całym świecie na roślinach z rodziny różowatych ( `` Rosaceae '' ) .
W Polsce największe znaczenie ma jako sprawca choroby brunatna plamistość liści gruszy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Diplocarpon , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten w 1878 r. opisał Paul Carl Moritz Sorauer nadając mu nazwę `` Stigmatea mespili '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Brian Charles Sutton w 1980 r .
