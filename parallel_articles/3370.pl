Diplocarpon rosae
Diplocarpon rosae F.A .
Wolf – gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Na różach wywołuje chorobę o nazwie czarna plamistość róży .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Diplocarpon , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Cykl rozwojowy .
Grzyb mikroskopijny , saprotrof i pasożyt obligatoryjny .
Infekcji pierwotnej dokonują zarodniki konidialne wytworzone wiosną na opadłych liściach .
Infekują roślinę zarówno przez górną , jak i dolną stronę liści , częściej jednak górną .
Strzępki grzyba wnikają w naskórek i rozprzestrzeniają się podskórnie w tkankach liścia .
