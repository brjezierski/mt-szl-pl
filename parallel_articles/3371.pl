Skorupowiec skalny
Skorupowiec skalny ( `` Diplotomma epipolium '' ( Ach . )
Arnold – gatunek grzybów z rodziny obrostowatych ( `` Physciaceae '' ) .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Diplotomma , Physciaceae , Caliciales , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1799 Erik Acharius nadając mu nazwę `` Lichen epipolius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1869 Ferdinand Christian Gustav Arnold , przenosząc go do rodzaju `` Diplotomma '' .
Synonimy nazwy naukowej :
Nazwę polską spójną z nazwą naukową podaje np .
strona Pienińskiego Parku Narodowego .
`` Krytyczna lista porostów i grzybów naporostowych Polski '' błędnie uznaje ten gatunek za synonim `` Buellia alboatra '' i podaje nazwę brunatka pstra .
