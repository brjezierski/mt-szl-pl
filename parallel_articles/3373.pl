Dir En Grey
– japoński zespół rockowy powstały 2 lutego 1997 roku w wyniku rozpadu grupy La : Sadie ’ s .
Podczas gdy skład grupy od początku pozostał niezmienny , liczne stylistyczne zmiany sprawiły , że ciężko określić gatunek granej przez nich muzyki ( jednakże najczęściej podstawą jest rock ) .
Pierwotnie byli częścią japońskiej sceny visual-kei , jednak w ostatnich latach zdecydowali się zrezygnować z ekscentrycznego ubioru .
Nazwa zespołu składa się z trzech słów , każde wzięte z innego języka .
„ Dir ” z niemieckiego , „ en ” z francuskiego oraz „ grey ” z angielskiego .
Kaoru utrzymuje , iż `` Dir En Grey '' znaczy `` szara moneta '' .
Innym tłumaczeniem tej nazwy może być : `` tobie w szarości '' .
Fani , mówiąc o grupie , zamiast pełnej nazwy używają często wyrażeń `` Deg '' oraz `` Diru '' .
