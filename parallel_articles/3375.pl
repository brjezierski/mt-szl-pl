Discostroma corticola
Discostroma corticola ( Fuckel ) Brockmann – gatunek grzybów mikroskopijnych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Discostroma , Sporocadaceae , Amphisphaeriales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1870 r. Karl Wilhelm Gottlieb Leopold Fuckel , nadając mu nazwę `` Sphaeria corticola '' .
Obecną , uznaną przez Index Fungorum nazwę nadała mu Ingrid Brockmann w 1876 r .
Ma 21 synonimów .
Niektóre z nich :
Morfologia .
Konidiofory z przegrodami , rozgałęzione , bezbarwne , gładkie , pokryte śluzem .
Komórki konidiotwórcze pojedyncze lub zintegrowane , butelkowate lub ampułkowate , czasami cylindryczne lub prawie cylindryczne , 6–20 × 1,5–4,5 μm , bezbarwne , gładkie , z maksymalnie trzema annelacjami .
Konidia przeważnie jajowate lub cylindryczne z zaokrąglonymi końcami , proste , bladobrązowe , przeważnie z 3-4-przegrodami , czasami 5-przegrodowe , końcowe przegrody połączone z komórką podstawną , cieńsze niż inne przegrody .
