Djakowica
Djakowica ( `` Ðakovica '' , `` Ђаковица '' ; alb .
`` Gjakova '' lub `` Gjakovë '' ) – miasto leżące w południowo-zachodnim Kosowie w regionie Peć .
Czwarte co do wielkości miasto kraju .
Ośrodek przemysłu ( włókienniczego , spożywczego ) i rzemiosła ( złotnictwo ) ; meczet ( XVI wiek ) , domy w stylu bałkańskim i orientalnym .
W pobliżu miasta wydobycie rud chromu .
Nazwa tej miejscowości może wywodzić się od słowa `` đakon '' czyt .
dźakon ( `` diakon '' po serbsko-chorwacku ) i prawosławnego klasztoru Devič .
Przed wojną na Bałkanach w Djakowicy mieszkało 61,4 tys .
ludzi , z czego 89 % stanowili kosowscy Albańczycy , 4 % Serbowie a 7 % inne narodowości .
Miejscowość poważnie ucierpiała podczas wojny .
Zbrodnie wojenne dotknęły początkowo ludność pochodzenia albańskiego .
Musieli uciekać ze swoich domów , wiele osób zginęło .
Po ustaniu walk Albańczycy wrócili , natomiast w obawie przed zemstą swoje domy musiała opuścić cała ludność niealbańska .
W 2016 w Djakowicy rozegrano Mistrzostwa Europy w szachach .
