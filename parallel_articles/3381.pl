Dialekt
Dialekt , narzecze – wieloznaczny termin lingwistyczny .
W najogólniejszym znaczeniu dialekt to odmiana języka , która odznacza się swoistymi cechami fonetycznymi , leksykalnymi czy gramatycznymi , odróżniającymi ją od innych form tego języka .
Wszystkie dialekty w jednakowy sposób rządzą się zespołem zasad gramatycznych , choć ich systemy bywają silnie zróżnicowane .
Różnice dialektalne wykazują zasadniczo wszystkie języki świata , przy czym tylko niektóre z nich mają swoją odmianę ogólną , tzw .
dialekt standardowy .
Pewną formą dialektu ( wariantem językowym o ustalonej strukturze ) posługuje się każdy użytkownik języka .
Czynniki społeczne prowadzą jednak do porzucania środków gwarowych na rzecz elementów języka standardowego , uważanych za neutralne geograficznie .
Zakres odrębności dialektalnych różni się w zależności od języka .
Niektóre języki , jak np .
rosyjski , przejawiają je w stosunkowo niewielkim stopniu , a istniejące dialekty mają dość szeroki zasięg geograficzny .
W przypadku innych języków , takich jak niemiecki , można wyróżnić dużą liczbę wciąż żywych dialektów .
Same dialekty mogą być zróżnicowane wewnętrznie i wykazywać drobne różnice na poziomie lokalnym ( w języku polskim mówi się wówczas o gwarach ) .
Granice między poszczególnymi dialektami bywają nieostre i trudne do wyznaczenia .
Badaniem dialektów ( zwłaszcza regionalnych ) zajmuje się dział językoznawstwa – dialektologia .
W rozumieniu dialektologii tradycyjnej dialekt to byt przeciwstawiany językowi standardowemu .
Historia terminu .
Termin „ dialekt ” wywodzi się z greckiego słowa `` diálektos '' ( ) oznaczającego `` dyskurs , rozmowę , sposób mówienia '' , a które z kolei pochodzi od słów `` diá '' ( „ poprzez ” ) i `` legō '' ( „ mówię ” ) .
