Dla Elizy
Dla Elizy ( niem .
`` Für Elise '' ) – popularny tytuł `` Bagateli a-moll '' WoO 59 , jednej z najbardziej znanych w literaturze muzycznej miniatur fortepianowych , którą skomponował Ludwig van Beethoven .
Utwór powstał ok. 1810 roku .
Pochodzenie tytułu .
Biografowie Beethovena nie są zgodni co do tego , kim była tytułowa Eliza .
Według jednej z teorii , oryginalny tytuł brzmiał `` Für Therese '' , jako że adresatką utworu miała być znajoma kompozytora i córka jednego z lekarzy wiedeńskich , Therese Malfatti .
W 1810 r. kompozytor oświadczył się jej , ona zaś oświadczyny odrzuciła , a w roku 1816 została żoną szlachcica i urzędnika Wilhelma von Droßdika .
Inna wersja mówi , że Eliza była słynną „ Nieśmiertelną Ukochaną ” , do której Beethoven napisał swój słynny list ( zob .
`` List do Nieśmiertelnej Ukochanej '' ) , a której tożsamości biografom nigdy nie udało się ustalić .
Kiedy kompozycję opublikowano w 1865 r. , jej odkrywca Ludwig Nohl nadał jej tytuł `` Dla Elizy '' , pod którym znana jest do dziś .
Według innej teorii Elizą była sopranistka .
Struktura utworu .
Utwór o budowie ABACA utrzymany jest w metrum 3/8 .
Popularny motyw melodyczny , grany przez prawą rękę na tle rozłożonych akordów lewej ręki , oparty jest początkowo jedynie na dwóch funkcjach harmonicznych : a i E ( będącymi w utworze funkcją toniki i dominanty ) .
„ Odpowiedź ” ma wychylenie do trybu durowego : C , G , aby znów przejść w , a i E. Część B jest żywsza , w tonacji F-dur , z drobnymi pochodami sekundowymi .
Część C jest z kolei mroczniejsza , niespokojna ; charakteryzują ją crescenda , ostinatowy motyw w lewej ręce i ekstensywne użycie pedału zgłaśniającego .
