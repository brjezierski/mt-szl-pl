Dmitrij Mendelejew
Dmitrij Iwanowicz Mendelejew ( ; ur .
w Tobolsku , zm .
w Petersburgu ) – rosyjski chemik , odkrywca ( w 1869 ) prawa okresowości pierwiastków chemicznych , laureat Medalu Copleya .
Życiorys .
Dzieciństwo i studia .
Był najmłodszym z czternaściorga dzieci dyrektora gimnazjum w Tobolsku , nauczyciela literatury , dekabrysty i zesłańca Iwana Pawłowicza i Marii Dmitrijewnej ( z domu Korniljewej ) , która była właścicielką huty szkła .
Po śmierci ojca i pożarze , który zniszczył fabrykę matki , jako trzynastoletni chłopiec rozpoczął naukę w gimnazjum w Tobolsku .
Młody Mendelejew pod wpływem poglądów ojca pozostawał przez całe życie liberałem , co miało wielokrotnie wielki wpływ na jego los .
W 1849 zubożała rodzina Mendelejewów przeniosła się do Petersburga .
Tam rok później Dmitrij wstąpił w szeregi studentów wydziału matematyczno-fizycznego Głównego Instytutu Pedagogicznego .
Młodość .
Po studiach , w 1855 , ze względu na fatalny stan zdrowia spowodowany gruźlicą wyjechał na Krym , słynący z uzdrowiskowego klimatu .
Tutaj objął stanowisko wykładowcy nauk przyrodniczych w I Gimnazjum w Symferopolu .
Karierę dydaktyczną na południu przerwała jednak tocząca się wojna krymska .
Po całkowitym wyzdrowieniu wrócił w 1857 do Petersburga .
W okresie 1859–1861 prowadził prace badawcze nad zjawiskiem podciągania kapilarnego i włoskowatości , a także badał zagadnienia spektroskopii na Uniwersytecie w Heidelbergu .
Pod koniec sierpnia 1861 opublikował książkę o spektroskopii .
4 kwietnia 1862 za namową rodziny zaręczył się z Fieozwą Nikiticzną Leszczewą , z którą wstąpił w związek małżeński już 27 kwietnia w soborze św .
Mikołaja w Petersburgu .
Mieli dwoje dzieci : Władimira ( żeglarz , członek słynnej podróży wschodniej carewicza Mikołaja ) i Olgę , ale małżeństwo nie było szczęśliwe ze względu na zbyt wielką różnicę charakterów .
Nieustanne kłótnie spowodowały , że wkrótce Dmitrij i Fieozwa znaleźli się w separacji i zamieszkali oddzielnie .
W 1863 Mendelejew objął stanowiska : profesora chemii w Petersburskim Instytucie Technicznym oraz docenta na Petersburskim Uniwersytecie Państwowym .
Prowadzone przez niego wykłady z chemii teoretycznej i organicznej cieszyły się dużą popularnością wśród studentów .
Po doktoracie .
31 stycznia 1865 obronił publicznie rozprawę doktorską `` O połączeniach alkoholu z wodą '' , w której między innymi jako pierwszy wyjaśnił zjawisko kontrakcji objętości .
W 1867 uzyskał stałe zatrudnienie na uczelni .
