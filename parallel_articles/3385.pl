Dmitrij Sawastin
Dmitrij Sawastin ( ur .
1942 w Vulcănești ) – mołdawski rysownik pochodzenia gagauskiego .
W latach 1983–1993 uczył plastyki w szkole .
Jest aktywnym członkiem ruchu autonomii Gagauzji oraz klubu „ Gagauz Halkı ” ( `` lud gagauski '' ) .
W jego rysunkach przeważają motywy gagauskie – postacie z legend oraz dziewczyny gagauskie .
