Dmytrów
Dmytrów ( ukr .
Дмитрів ) – wieś na Ukrainie .
Należy do rejonu radziechowskiego w obwodzie lwowskim i liczy 1476 mieszkańców .
W II Rzeczypospolitej do 1934 roku wieś stanowiła samodzielną gminę jednostkową w powiecie radziechowskim w woj .
tarnopolskim .
W związku z reformą scaleniową została 1 sierpnia 1934 roku włączona do nowo utworzonej wiejskiej gminy zbiorowej Chołojów w tymże powiecie i województwie .
Po wojnie w wyniku zmiany granic wieś została włączona do Ukraińskiej SRR .
W Dmytrowie 17 sierpnia 1896 urodził się Józef Wittlin - polski poeta , prozaik i tłumacz .
