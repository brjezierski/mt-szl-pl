Dobromil ( stacja kolejowa )
Dobromil – stacja kolejowa w Dobromilu w obwodzie lwowskim Ukrainy .
Znajduje się 27 km od stacji Przemyśl Główny .
Do 1994 przez stacje przejeżdżały eksterytorialne pociągi z Przemyśla do Zagórza .
Obecnie na stacji dwa razy dziennie zatrzymuje się pociąg relacji Niżankowice-Sambor .
