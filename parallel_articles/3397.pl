Dobruška ( stacja kolejowa )
Dobruška ( czeski : `` Železniční stanice Dobruška '' ) – stacja kolejowa w miejscowości Dobruška , w kraju hradeckim , w Czechach .
Jest ważnym węzłem kolejowym o znaczeniu regionalnym .
Znajduje się na wysokości 285 m n.p.m .
Jest obsługiwana i zarządzana przez České dráhy .
Na stacji nie ma możliwości zakupu biletów , a obsługa podróżnych odbywa się w pociągu .
