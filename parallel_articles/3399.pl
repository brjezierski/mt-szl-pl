Dobrzejowice
Dobrzejowice – wieś w Polsce położona w województwie dolnośląskim , w powiecie głogowskim , w gminie Żukowice .
Podział administracyjny .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Dobrzejowice .
W latach 1975–1998 miejscowość administracyjnie należała do województwa legnickiego .
Nazwa .
Nazwa pochodzi od polskiego słowa oznaczającego `` dobro '' .
W roku 1295 w kronice łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księdze uposażeń biskupstwa wrocławskiego '' ) miejscowość wymieniona jako `` Dobrseiowitz '' .
Miejscowość nosiła również zgermanizowaną nazwę `` Doberwitz '' .
