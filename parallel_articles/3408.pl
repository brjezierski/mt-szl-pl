Dodoma ( region )
Dodoma – region ( `` mkoa '' ) w Tanzanii .
W 2002 roku region zamieszkiwało 1 692 025 osób .
W 2012 ludność wynosiła 2 083 588 osób , w tym 1 014 974 mężczyzn i 1 068 614 kobiet , zamieszkałych w 453 844 gospodarstwach domowych .
