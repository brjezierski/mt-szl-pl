Doetinchem
Doetinchem – holenderskie miasto i gmina leżące na wschodzie kraju nad rzeką IJssel .
Miasto znajduje się w regionie Achterhoek będącym częścią prowincji znanej jako Geldria .
Doetinchem 1 stycznia 2005 roku zamieszkiwało 56 700 mieszkańców – zatem pod względem ludności jest to największe miasto w Achterhoek .
Powierzchnia miasta wynosi 79,66 km² ( z czego na zbiorniki wodne przypada 1,50 km² ) .
Na skutek reorganizacji administracyjnych 1 stycznia 2005 roku do Doetinchem włączona została sąsiednia miejscowość Wehl .
