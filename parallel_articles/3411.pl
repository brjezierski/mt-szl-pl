Dogana
Dogana – największe miasto San Marino znajdujące się w zamku Serravalle .
Liczy około 4500 mieszkańców co stanowi ok. 15 % populacji państwa .
W Doganie znajduje się większość urzędów .
Mimo to Dogana nie jest stolicą Zamku .
Dogana leży przy granicy z Włochami i jest najczęściej pierwszym miastem odwiedzanym przez turystów wjeżdżających do państwa .
