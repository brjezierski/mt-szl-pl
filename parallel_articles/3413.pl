Dohtor Miód , właśc .
Marcin Witas ( ur .
21 kwietnia 1976 w Piekarach Śląskich ) – śląski wykonawca hip-hopowy ; raper .
Twórczość .
Dohtor Miód rozpoczął karierę w 1991 roku ; albumem `` Votan B . ''
( znanym do tej pory jedynie w najbliższym otoczeniu artysty ) .
