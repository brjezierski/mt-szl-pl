Dokken
Dokken – amerykański zespół wykonujący heavy metal i hard rock , założony w 1978 roku .
Grupa sprzedała ponad 10 milionów płyt na całym świecie .
Historia zespołu .
Wczesne lata ( 1978-1983 ) .
Dokken został założony w 1978 roku przez Dona Dokkena , wraz z Jimem Monanterasem , Gregiem Leonem i Mickiem Brownem .
Przedtem Don dawał koncerty w Los Angeles z zespołem „ Airborn ” , jednak był zmuszony zmienić nazwę na Dokken , gdyż grupa pod taką nazwą już istniała i miała podpisany kontrakt płytowy .
W 1979 roku , Don Dokken poleciał do Niemiec , aby nagrać wokal wspierający na nadchodzący album zespołu Scorpions , `` Blackout '' .
Miało być to wsparcie dla wokalisty zespołu , Klausa Meine , który cierpiał na dolegliwości strun głosowych .
