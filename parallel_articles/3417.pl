Dolaszewo
Dolaszewo ( dawniej : niem .
`` Hasenberg '' ) – wieś krajeńska w Polsce położona w województwie wielkopolskim , w powiecie pilskim , w gminie Szydłowo .
Od kilku lat wieś intensywnie się rozbudowuje .
Wieś królewska należała do starostwa ujskiego , pod koniec XVI wieku leżała w powiecie poznańskim województwa poznańskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa pilskiego .
Dawniej Dolaszewo było zwane Zajęczą Górką .
Od początku swego istnienia należało do starostwa ujsko–pilskiego .
W 1585 roku wieś była powtórnie lokowana .
