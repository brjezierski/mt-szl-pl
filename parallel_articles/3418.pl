Język dołgański
Język dołgański ( Дулҕан ) – język z rodziny tureckiej , używany przez około 1000 osób zamieszkałych na półwyspie Tajmyr w Rosji .
Użytkownicy języka to lud nazywany Dołganami .
Słowo „ Дулҕан ” oznacza `` ludzi żyjących przy środkowym biegu rzeki '' i odnosi się prawdopodobnie do położenia geograficznego miejsca pochodzenia Dołgan .
Język ma zasięg lokalny i jego funkcjonowanie jest ograniczone do wąskiego obszaru ; w ciągu ostatnich lat spadła liczba osób posługujących się tym językiem .
Według stanu z 2010 r. język liczył tylko około 1050 użytkowników .
