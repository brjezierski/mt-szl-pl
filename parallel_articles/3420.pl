Barcice Dolne
Barcice Dolne – wieś w Polsce położona w województwie małopolskim , w powiecie nowosądeckim , w gminie Stary Sącz .
W latach 1975–1998 miejscowość administracyjnie należała do województwa nowosądeckiego .
Barcice Dolne to również nazwa sołectwa , jako jednostki pomocniczej gminy Stary Sącz , utworzonego na całym obszarze tej miejscowości ( siedzibą sołectwa jest wieś sołecka ) .
Należy dodać , iż w sąsiedztwie Barcic Dolnych , od południowej strony , leżą Barcice , z których w 1910 roku wyodrębniły się Barcice Dolne .
Administracyjnie są to odrębne miejscowości .
Opis .
Barcice Dolne to miejscowość w całości położona na równinie , na lewym brzegu Popradu , na wysokości 325 m n.p.m .
Jest najmniejszym obszarowo sołectwem gminy Stary Sącz .
