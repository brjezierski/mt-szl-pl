Osiny Dolne
Osiny Dolne – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Jadwigi w Mokobodach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
We wsi działa założona w 1967 roku jednostka ochotniczej straży pożarnej .
