Ustrzyki Dolne ( stacja kolejowa )
Ustrzyki Dolne – stacja kolejowa w Ustrzykach Dolnych , w województwie podkarpackim , w Polsce .
Z powodu wyłączenia z eksploatacji linii kolejowej 108 na odcinku Uherce – Krościenko pociągi nie obsługiwały stacji w latach 2010–2022 ..
Dzięki inicjatywie samorządu województwa podkarpackiego 12 czerwca 2022 roku przywrócono połączenia pasażerskie Sanok – Ustrzyki Dolne obsługiwane przez przewoźnika Polregio .
16 lipca przewoźnik SKPL uruchomił podobne połączenie Ustrzyk Dolnych do Sanoka z większą ilością par .
