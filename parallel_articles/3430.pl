Mszana Dolna
Mszana Dolna – miasto w województwie małopolskim , w powiecie limanowskim , stolica Górali Zagórzańskich .
Mszana Dolna uzyskała lokację miejską w 1346 roku , zdegradowana przed 1409 rokiem , ponowne nadanie praw miejskich w 1952 roku .
W latach 1975–1998 miasto administracyjnie należało do województwa nowosądeckiego .
W latach 1934–1952 była to gmina Mszana Dolna I. Jest siedzibą gminy Mszana Dolna .
Położenie .
Mszana Dolna leży w zachodniej części Beskidu Wyspowego w zlewisku rzeki Raby , 50 km na południe od Krakowa , 50 km na północ od Zakopanego i 12 km na północny wschód od Rabki .
Jest światowym przykładem okna tektonicznego .
Kotlina , w której miasto jest położone , otoczona jest szczytami Beskidu Wyspowego : Lubogoszcz , Czarny Dział , Grunwald , Luboń Wielki ( 1022 m n.p.m . )
