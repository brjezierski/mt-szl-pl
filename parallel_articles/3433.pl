Alfabet dolnołużycki
Alfabet dolnołużycki – alfabet oparty na piśmie łacińskim , służący do zapisu języka dolnołużyckiego .
Składa się z następujących liter :
a , b , c , č , ć , d , e , ě , f , g , h , ch , i , j , k , ł , l , m , n , ń , o , p , r , ŕ , s , ś , š , t , u , w , y , z , ž , ź .
W ortografii dolnołużyckiej występuje także znak „ ó ” , który odpowiada jednak innym głoskom niż polskie „ ó ” – w zależności od regionu dolnołużyckie ó wymawia się jak polskie „ ó ” , „ e ” lub „ y ” .
