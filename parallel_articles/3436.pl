Województwo dolnośląskie
Województwo dolnośląskie – jednostka podziału administracyjnego Polski , jedno z 16 województw , ze stolicą we Wrocławiu .
Zostało utworzone w 1999 roku z ziem poprzednich województw wrocławskiego , legnickiego , jeleniogórskiego , wałbrzyskiego i części leszczyńskiego oraz kaliskiego .
Województwo to jest położone na południowym zachodzie Polski , obejmuje zachodnią część historycznego Śląska , czyli większość Dolnego Śląska , a także ziemię kłodzką oraz wschodnie Łużyce Górne .
Według danych z 30 czerwca 2020 roku województwo zamieszkiwało ok. 2,9 mln osób .
