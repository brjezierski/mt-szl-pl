Niederschlesischer Oberlausitzkreis
Niederschlesischer Oberlausitzkreis ( głuż .
`` Delnjošlesko-hornjołužiski wokrjes '' ) – były powiat w rejencji Drezno w niemieckim kraju związkowym Saksonia .
Siedzibą Niederschlesischer Oberlausitzkreis było Niska .
Od 1 sierpnia 2008 obszar powiatu wchodzi do nowo powstałego powiatu Görlitz .
