Dla gromady ustalono 23 członków gromadzkiej rady narodowej .
31 grudnia 1961 do gromady Brzeg Dolny włączono obszar zniesionej gromady Pogalewo Wielkie w tymże powiecie .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 w powiecie wołowskim reaktywowano zniesioną w 1954 roku gminę Brzeg Dolny .
