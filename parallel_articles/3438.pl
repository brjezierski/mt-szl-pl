Brzeg Dolny ( gromada 1960–1972 )
Brzeg Dolny – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Brzeg Dolny z siedzibą GRN w mieście Brzegu Dolnym ( nie wchodzącym w skład gromady ) utworzono 1 stycznia 1960 w powiecie wołowskim w woj .
