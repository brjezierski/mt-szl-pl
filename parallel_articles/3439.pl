Gorzeń Dolny
Gorzeń Dolny – wieś w Polsce administracyjnie należąca do województwa małopolskiego , powiatu wadowickiego i gminy Wadowice .
Położona na lewym brzegu rzeki Skawy , na skraju Beskidu Małego , u podnóża Goryczkowca ( 375 m n.p.m .
) , stanowiącego najwyżej położony punkt wsi .
Graniczy z miejscowościami : Zawadka , Jaroszowice , Gorzeń Górny i Wadowice .
Przez wieś przebiega odcinek drogi krajowej nr 28 ( Zator – Medyka ) .
Obszar wsi obejmuje 156 ha .
Przeważają użytki rolne ( 56 % powierzchni ) .
Gorzeń Dolny zamieszkuje 471 osób ( 2008 ) .
