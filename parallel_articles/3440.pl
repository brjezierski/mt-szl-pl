Kazimierz Dolny
Kazimierz Dolny ( `` Kuzmer '' lub `` Kuzmir '' ; do 1927 Kazimierz , Kazimierz nad Wisłą ) – miasto w Polsce w województwie lubelskim , w powiecie puławskim , nad Wisłą , w Małopolskim Przełomie Wisły , w zachodniej części Płaskowyżu Nałęczowskiego .
Pod względem historycznym Kazimierz Dolny położony jest w Małopolsce , początkowo należał do ziemi sandomierskiej , a następnie do ziemi lubelskiej .
Jest siedzibą władz miejsko-wiejskiej gminy Kazimierz Dolny .
Część trójkąta turystycznego : Puławy – Kazimierz Dolny – Nałęczów .
Był miastem królewskim Korony Królestwa Polskiego w starostwie kazimierskim województwa lubelskiego w 1786 roku .
Kazimierz Dolny uzyskał prawo składu w 1335 roku .
W latach 1975–1998 miasto administracyjnie należało do województwa lubelskiego .
Według danych GUS z 31 grudnia 2019 r. Kazimierz Dolny liczył 2530 mieszkańców .
