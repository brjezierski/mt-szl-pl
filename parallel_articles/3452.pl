Domaradzka Kuźnia
Domaradzka Kuźnia ( niem .
`` Dammfelder Hammer '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie namysłowskim , w gminie Pokój .
W obrębie Domaradzkiej Kuźni wyróżniona jest administracyjnie część miejscowości – Jaginów .
Historia .
Wieś zamieszkiwała opolska grupa etnograficzna Leśnioków .
, natomiast pierwsze wzmianki o Jagunowie – kolonii Domaradzkiej Kuźni , pochodzą z 1651 r. Na tych terenach wśród lasów znaleziono rudę , zwaną wtedy „ resenerz ” .
W 1590 r. przybył na teren miejscowości mistrz hutniczy , niejaki Adam Kuschnik z Kadłuba Turawskiego .
Otrzymał on zezwolenie na budowę kuźni nad rzeką Stobrawą , w której wytapiano rudę .
Za korzystanie z prawa wodnego płacił 50 talarów rocznie .
Huta przetrwała do 1718 r. W 1666 r. Daniel Kotliński wydzierżawił kuźnię i okoliczne lasy , za które zapłacił 600 niemieckich talarów .
Otrzymał zezwolenie na wyrób drewna , które przerabiał w kuźni na węgiel drzewny .
Domaradzka Kuźnia dzieliła się na wieś i kolonię ( obecna ul .
1 Maja , potocznie zwana „ Kolonistami ” ) .
Mieszkańcy wsi trudnili się rolnictwem i handlem .
