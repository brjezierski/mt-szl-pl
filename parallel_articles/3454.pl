Donald Tusk
( ur .
22 kwietnia 1957 w Gdańsku ) – polski polityk , z wykształcenia historyk .
Współzałożyciel Kongresu Liberalno-Demokratycznego ( 1990 ) oraz Platformy Obywatelskiej ( 2001 ) .
Przewodniczący KLD ( w latach 1991–1994 ) oraz PO ( w latach 2003–2014 i od 2021 ) , pełniący obowiązki przewodniczącego PO w 2021 .
W latach 2007–2014 prezes Rady Ministrów .
W latach 2014–2019 przewodniczący Rady Europejskiej , w latach 2019–2022 przewodniczący Europejskiej Partii Ludowej .
Poseł na Sejm I , IV , V , VI i VII kadencji ( 1991–1993 , 2001–2014 ) , w latach 1997–2001 senator i wicemarszałek Senatu IV kadencji , w latach 2001–2005 wicemarszałek Sejmu IV kadencji , w latach 2007–2009 przewodniczący Komitetu Integracji Europejskiej ; jest osobą najdłużej sprawującą urząd premiera w III RP .
Kandydat na urząd prezydenta RP w wyborach w 2005 .
Życiorys .
Wykształcenie .
W latach 1964–1972 uczęszczał do Szkoły Podstawowej nr 57 w Gdańsku .
W 1976 został absolwentem I Liceum Ogólnokształcącego im .
Mikołaja Kopernika w Gdańsku .
W 1980 ukończył studia z zakresu historii na Wydziale Humanistycznym Uniwersytetu Gdańskiego .
Jego praca magisterska dotyczyła kształtowania się legendy Józefa Piłsudskiego w przedwojennych czasopismach .
Działalność opozycyjna w PRL .
W działalność opozycyjną wobec władz PRL zaangażował się w czasie studiów na Uniwersytecie Gdańskim , gdzie uczestniczył w tworzeniu lokalnego Studenckiego Komitetu Solidarności , co było reakcją na zamordowanie w Krakowie przez Służbę Bezpieczeństwa Stanisława Pyjasa .
Rozpoczął także współpracę z Bogdanem Borusewiczem i Wolnymi Związkami Zawodowymi Wybrzeża , od 1977 do 1979 brał udział w spotkaniach informacyjnych i samokształceniowych organizowanych przez środowiska opozycyjne .
W trakcie strajku w sierpniu 1980 był jednym z autorów apelu na rzecz powołania niezależnej organizacji studenckiej – Niezależnego Zrzeszenia Studentów Polskich .
We wrześniu został członkiem Prezydium Tymczasowego Komitetu Założycielskiego NZSP na Uniwersytecie Gdańskim .
Był przewodniczącym Komisji Zakładowej Niezależnego Samorządnego Związku Zawodowego „ Solidarność ” w Wydawnictwie Morskim i dziennikarzem wydawanego przez związek tygodnika „ Samorządność ” .
Od 1982 współpracował z pismami podziemnymi .
Był jednym ze współpracowników Lecha Bądkowskiego .
Pisał do kaszubskiego miesięcznika „ Pomerania ” .
Przez kilka lat utrzymywał się z pracy fizycznej w założonej przez Macieja Płażyńskiego spółdzielni „ Świetlik ” .
Był autorem tekstów opublikowanej w 1985 przez wydawnictwo Sport i Turystyka książki pt .
