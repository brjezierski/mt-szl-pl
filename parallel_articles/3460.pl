Czyreniówka rozpostarta
Czyreniówka rozpostarta ( `` Donkioporia expansa '' ( Desm . )
Kotl .
& amp ; Pouzar ) – gatunek grzybów należący do rodziny żagwiowatych ( `` Polyporaceae '' ) .
Na terenie Polski do 2021 r. nieznany .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Donkioporia , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1823 r. Jean Baptiste Henri Joseph Desmazières nadając mu nazwę `` Boletus expansus '' .
Potem zaliczany był do różnych innych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1973 r. František Kotlaba i Zdeněk Pouzar przenosząc go do utworzonego przez siebie rodzaju `` Donkioporia '' .
