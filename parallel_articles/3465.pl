Dorota Abbe
Dorota Abbe ( ur .
13 maja 1973 w Poznaniu ) – polska aktorka teatralna , filmowa i telewizyjna , również wokalistka .
Życiorys .
Jest córką aktora Henryka Abbe .
W 1999 roku została absolwentką Wydziału Aktorskiego PWSFTviT w Łodzi .
W latach 1999–2000 zatrudniona była w Teatrze Polskim w Bielsku-Białej .
W latach 2000–2011 etatowo związana z wrocławskim Teatrem Współczesnym im .
Wiercińskiego , z którym obecnie nadal współpracuje .
Od 2011 roku aktorka Teatru Nowego w Poznaniu .
