Doda ( Dżammu i Kaszmir )
Doda – miejscowość w Indiach w dystrykcie Doda w Dżammu i Kaszmir .
Według spisu powszechnego z 2011 roku zamieszkana przez 21 605 osób .
Doda położona jest na wysokości 1149 m n.p.m. , nad rzeką Dźhelam , około 80 km na północny wschód od Dżammu .
Z zachodu na wschód przebiega przez nią droga krajowa 244 z Batote do Khanbal .
Demografia .
Spośród 21 605 mieszkańców w 2011 roku 12 506 stanowili mężczyźni , a 9 099 kobiety .
66,45 % mieszkańców wyznaje islam , a 32,62 % hinduizm .
