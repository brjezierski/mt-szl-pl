Dortmund ( koń wyścigowy )
Dortmund ( ur .
7 lutego 2012 ) – emerytowany amerykański koń wyścigowy pełnej krwi angielskiej .
Syn ogiera Big Brown i klaczy Our Josephina .
Życiorys .
Został wyhodowany w Kentucky przez Emilie Gerlinde Fojan .
Jest znany z tego , że jest bardzo duży – jako trzylatek mierzył ponad 172 cm .
wzrostu , podczas gdy inne konie wyścigowe w jego wieku mierzą ok. 164 cm .
Fojan chciała go sprzedać , kiedy był źrebakiem , jednak nie osiągnął ceny wywoławczej .
Powiedziała o nim : „ Był taki duży ; wszyscy obawiali się jego wzrostu ” .
Udało jej się go sprzedać w lipcu 2013 za 90 tysięcy dolarów .
Dziesięć miesięcy później został sprzedany za 140 tysięcy dolarów .
Kupił go przedsiębiorca Kaleem Shah .
Nazwał go na cześć drużyny piłkarskiej Borussia Dortmund .
W latach 2014–2016 trenował go Bob Baffert , a przez pierwsze siedem wyścigów dosiadał go Martin Garcia .
