Dositeewo
Dositeewo ( bułg .
`` Доситеево '' ) – wieś w południowej Bułgarii , w obwodzie Chaskowo , w gminie Charmanli .
Według danych Narodowego Instytutu Statystycznego , 31 grudnia 2011 roku wieś liczyła 306 mieszkańców .
Położenie .
Wioska znajduje się w skrajnych częściach południowo-zachodniego Sakaru , w dolinie Maricy .
Historia .
Dawniej wieś nazywała się Sjuli kjoj .
Gospodarka .
Głównym zajęciem mieszkańców jest rolnictwo i hodowla zwierząt .
