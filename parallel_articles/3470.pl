Douai
Douai – miejscowość , gmina we Francji , w regionie Hauts-de-France , w departamencie Nord .
Miasto leży nad rzeką Scarpe ( dopływ Skaldy ) i Kanałem Sensée , 35 km na południe od Lille .
Według danych na rok 1990 gminę zamieszkiwało 42 175 osób ( zespół miejski 200 tys .
( 1990 ) ) , a gęstość zaludnienia wynosiła 2500 osób/km² .
Wśród 1549 gmin regionu Nord-Pas-de-Calais Douai plasuje się na 9. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 95 .
