Dōjin
– japońskie słowo oznaczające koterię , klikę , grupę osób o zbliżonych celach lub zainteresowaniach .
We współczesnej japońszczyźnie oznacza również grupę hobbystów i fanów publikujących własnym nakładem różne materiały , jak mangi , powieści , poezję , albumy z fotografiami , muzykę i gry komputerowe .
Część takich publikacji jest klasyfikowana jako fan fiction , jednak grupy te publikują także prace oryginalne .
