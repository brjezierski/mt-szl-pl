Dowiaty ( rejon brasławski )
Dowiaty – dawna osada .
Tereny na których leżała znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Achremowce .
Historia .
W czasach zaborów dwór w gminie Jody , w powiecie dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
Pod koniec XIX wieku należał do Hanaków .
W latach 1921–1945 osada leżała w Polsce , w województwie wileńskim , w powiecie dziśnieńskim ( od 1926 w powiecie brasławskim ) , w gminie Jody .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 14 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 1 domu zamieszkiwało 8 osób .
Wierni należeli do parafii rzymskokatolickiej i prawosławnej w Zamoszach .
Miejscowość podlegała pod Sąd Grodzki w Brasławiu i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Jodach .
