Down Low
Down Low – niemiecka grupa hip-hopowa założona w 1995 roku w Kaiserslautern ( Nadrenia-Palatynat ) .
Członkami grupy są dwaj czarnoskórzy Amerykanie : raper Joe Thompson i wokalista Mike Dalien .
Grupa znana jest z takich utworów jak : „ Vision of Life ” , „ Murder ” , „ Potion ” , „ Lovething ” oraz „ Johnny B ” .
Zespół wydał osiem albumów .
