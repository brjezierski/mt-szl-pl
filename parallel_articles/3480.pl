Dołżańsk
Dołżańsk ( , `` Dowżanśk '' ) – miasto na Ukrainie w obwodzie ługańskim .
Ośrodek przemysłu materiałów budowlanych ; wydobycia węgla kamiennego .
Historia .
Prawa miejskie posiada od 1938 .
W 1938 roku zaczęto wydawać gazetę .
Do 12 maja 2016 roku nosiło nazwę Swerdłowśk ( ukr .
Свердловськ ) .
Od 2014 roku znajduje się pod kontrolą samozwańczej Ługańskiej Republiki Ludowej , więc zmiana nazwy nie została w praktyce przeprowadzona .
