Dołgoprudnyj
Dołgoprudnyj ( ros .
`` Долгопрудный '' ) – miasto w Rosji , w obwodzie moskiewskim , położone ok. 20 km na północ od centrum Moskwy .
Nazwa pochodzi od rosyjskiego `` Долгий пруд '' , w dosłownym przekładzie `` Długostawie '' .
Długi i wąski staw znajduje się w północno-wschodniej części miasta .
Miasto ma powierzchnię 30,5 km² .
Według danych ze spisu ludności w 2020 liczyło 116 038 mieszkańców .
W miejscu Dołgoprudnego w XVII wieku istniała osada Winogradowo .
Na początku XX wieku doprowadzono tam kolej , stację kolejową zbudowano w 1914 .
Od lat 30 .
XX wieku ośrodek przemysłu lotniczego .
W 1951 przeniesiono do Dołgoprudnego Moskiewski Instytut Fizyki i Technologii .
Obecnie ośrodek przemysłu maszynowego i chemicznego oraz miasto-sypialnia dla Moskwy .
W 1957 miejscowość uzyskała prawa miejskie .
Miasto jest ośrodkiem rosyjskiego rodzimowierstwa , pochodzi stamtąd m.in .
zespół pagan-folkmetalowy Arkona .
