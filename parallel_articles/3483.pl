Drabina ( narzędzie tortur )
Drabina – rodzaj narzędzia służącego do torturowania podejrzanych .
Stosowano je głównie w mniejszych miastach i na wsiach .
W większych salach tortur korzystano zwykle z łoża sprawiedliwości .
Drabina stanowiła namiastkę łoża sprawiedliwości .
W przeciwieństwie do poziomej ławy tortur , ta opierana była o ścianę i ustawiana praktycznie pionowo .
Służyła do rozciągania i podciągania ciała torturowanych osób .
W tym celu podejrzanego przywiązywano za ręce i nogi do drabiny , a następnie za pomocą specjalnego kołowrotu naciągano krępujące go sznury .
Czasami nogi torturowanego obciążano dodatkowo kamiennymi odważnikami .
Środkowy szczebel drabiny posiadał również często kolce , które dodatkowo zwiększały zadawany podczas przesłuchania ból .
Drabinę jako narzędzie tortur wymieniał m.in .
