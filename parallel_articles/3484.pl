Drach
Drach – powieść Szczepana Twardocha , opublikowana w 2014 roku .
Autor otrzymał za nią Nagrodę Fundacji im .
Kościelskich ; utwór znalazł się też w finale Nagrody Literackiej Nike .
Akcja powieści rozgrywa się od 1906 do 2014 roku , głównie na terenach pomiędzy Gliwicami a Rybnikiem .
Przedstawia losy śląskiej rodziny Magnorów i Gemanderów na tle wydarzeń historycznych .
Wydarzenia nie pojawiają się chronologicznie – narracja przeskakuje pomiędzy kolejnymi latami , wielokrotnie w ciągu jednego rozdziału , a nawet strony .
Książka napisana jest w języku polskim , jednak występują w niej liczne wtrącenia w innych językach – niemieckim czy rosyjskim .
Autor celowo zrezygnował z opatrywania wtrąceń przypisami , chcąc , aby czytelnikowi towarzyszyło podczas lektury poczucie obcości .
10 października 2018 r. , nakładem Wydawnictwa Literackiego ukazał się przekład powieści na język ( etnolekt ) śląski , dokonany przez Grzegorza Kulika .
Tytuł powieści w języku śląskim oznacza smoka , gałgana lub latawiec .
W 2018 miała miejsce premiera , opartego na powieści , spektaklu `` Drach '' wyreżyserowanego przez Roberta Talarczyka .
Spektakl powstał w koprodukcji Teatru Śląskiego im .
Stanisława Wyspiańskiego w Katowicach i Teatru Ziemi Rybnickiej .
