DragonForce
DragonForce ( czasami nazwa zapisywana jako `` Dragonforce '' ) – zespół muzyczny z Londynu grający power metal .
Historia .
Zespół został założony we wrześniu 1999 roku i wcześniej znany jako DragonHeart .
Zespół zmienił nazwę w grudniu 2001 roku , by uniknąć pomyłek z innym zespołem , firmą fonograficzną i filmem pod tymże tytułem .
Zespół tworzy dwóch gitarzystów , Herman Li i Sam Totman , oraz klawiszowiec Vadim Pruzhanov .
Ten skład został uzupełniony przez basistę Freda Leclerq i perkusistę Davida Mackintosha .
Muzycy pochodzą z wielu regionów świata – Hongkongu , Anglii , Nowej Zelandii , Południowej Afryki , Ukrainy .
Wczesna muzyka DragonForce to nowoczesny power metal .
Z czasem ich styl wyewoluował i stworzyli charakterystyczne dla siebie brzmienie .
Poprzez połączenie wielu gatunków muzycznych , jak np .
heavy metalu , power metalu , speed metalu , thrash metalu , death metalu , hard rocka , a także muzyki z gier komputerowych .
