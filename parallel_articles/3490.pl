Mielenko Drawskie
Mielenko Drawskie ( niem .
`` Klein Mellen '' ) – wieś sołecka w Polsce położona w województwie zachodniopomorskim , w powiecie drawskim , w gminie Drawsko Pomorskie .
W latach 1954–1961 wieś należała i była siedzibą władz gromady Mielenko Drawskie , po jej zniesieniu w gromadzie Drawsko Pomorskie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa koszalińskiego .
W roku 2007 wieś liczyła 235 mieszkańców .
Wsie wchodzące w skład sołectwa : Oleszno , Woliczno , Ziemsko .
Geografia .
Wieś leży ok. 4,5 km na południowy zachód od Drawska Pomorskiego , ok. 50 m na zachód od drogi wojewódzkiej nr 175 .
Miejscowość znajduje się na terenie sandru Drawy , gdzie wody wypływające z topniejącego lodowca nagromadziły grube pokłady piasków i żwirów , obecnie eksploatowane gospodarczo w miejscowej kopalni kruszywa .
