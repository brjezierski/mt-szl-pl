Drepanopeziza populorum ( Desm . )
Höhn .
– gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Grzyb mikroskopijny , jeden z fitopatogenów wywołujących zgorzel liści topoli .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Drepanopeziza , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1857 r. Jean Baptiste Henri Joseph Desmazières nadając mu nazwę `` Trochila populorum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Franz von Höhnel w 1917 r .
Synonimy :
Morfologia i rozwój .
Patogen zimuje na opadłych liściach .
