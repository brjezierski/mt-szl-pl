Drepanopeziza punctiformis
Drepanopeziza punctiformis Gremmen – gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Grzyb mikroskopijny , jeden z fitopatogenów wywołujących zgorzel liści topoli .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Drepanopeziza , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia i rozwój .
Patogen zimuje na opadłych liściach .
Tworzy na nich soczewkowate owocniki typu apotecjum o rozmiarach 100–200 × 70–100 µm zawierające worki o rozmiarze 90–115 × 11–14 µm i wstawki .
Powstają w nich jednokomórkowe , hialinowe askospory o rozmiarach 9–14 × 3–7 µm .
Wiosną zarodniki te roznoszone przez wiatr dokonują infekcji pierwotnej porażając młode liście topoli .
Na obydwu stronach porażonych liści wiosną pojawiają się niewielkie , brązowe plamki o średnicy 1-3 mm .
