Drepanopeziza ribis
Drepanopeziza ribis ( Rehm ex Kleb . )
Höhn . )
– gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Pasożyt roślin wywołujący chorobę o nazwie antraknoza liści porzeczek .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Drepanopeziza , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1905 r. Heinrich Rehm i Heinrich Klebahn nadając mu nazwę `` Pseudopeziza ribis '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu w 1945 r. Franz Xaver Höhnel .
