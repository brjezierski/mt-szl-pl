Drezno
Drezno ( , , , dawniej `` Drezdno '' ) – miasto we wschodnich Niemczech , na Pogórzu Zachodniosudeckim , położone nad Łabą , stolica kraju związkowego Saksonia .
Aglomeracja drezdeńska liczy ok. 1,036 mln mieszkańców ( 2004 ) .
Analiza śladów archeologicznych wskazuje na to , że zasiedlenie obszaru , na którym powstało miasto , nastąpiło już w epoce kamienia .
W dokumentach zachowanych z 1206 nadmieniono po raz pierwszy istnienie Drezna , które następnie awansowało do rangi miasta-rezydencji Elektorów Rzeszy , króla Polski , a następnie króla Saksonii .
Drezno pełni rolę gospodarczego i kulturalnego centrum Saksonii .
Stanowi duży węzeł komunikacyjny ( lotnisko międzynarodowe ) , z ośrodkami przemysłu maszynowego , produkcji klisz fotograficznych , przemysłu chemicznego i farmaceutycznego .
Miasto jest ośrodkiem turystycznym , zwanym „ miastem baroku ” lub „ Florencją Północy ” , znajduje się tu wiele zabytków i muzeów , słynna jest Galeria Obrazów ( `` Gemäldegalerie Alte Meister '' ) .
Znajduje się tu również fabryka samochodów Volkswagen .
Drezno jest obok Lipska centralnym miastem obszaru metropolitalnego centralnych Niemiec , w skład którego wchodzi też dziesięć innych miast z krajów związkowych Saksonii , Saksonii-Anhalt i Turyngii .
Nazwa .
Nazwa miejscowości wywodzi się od osady założonej w tym miejscu przez Słowian połabskich o nazwie `` Drezdno '' – stąd używa się odmiany w formie przymiotnikowej „ drezdeński ” .
Według językoznawców bezpośrednio pochodzi ona od słowiańskiego słowa „ drezga ” ( pol .
„ drzazga ” ) .
„ Drežd ’ ane ” oznacza osadę ludzi mieszkających w lesie bądź „ leśnych ludzi ” .
Niemiecka nazwa `` Dresden '' jest zgermanizowaną górnołużycką nazwą miejscowości `` Drježdźany '' ( pol .
Drężdżany ) .
Podaje się również inne znaczenie , wywodząc nazwę od starołużyckiego `` Drežďany '' , oznaczającego „ mieszkańców mokradeł ” .
