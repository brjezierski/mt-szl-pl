Drewno ( technika )
Drewno – surowiec drzewny otrzymywany ze ściętych drzew i formowany przez obróbkę w różnego rodzaju sortymenty .
Zajmuje przestrzeń pomiędzy rdzeniem a warstwą łyka i kory .
Pod względem technicznym drewno jest naturalnym materiałem kompozytowym o osnowie polimerowej wzmacniany ciągłymi włóknami polimerowymi , którymi są podłużne komórki zorientowane jednoosiowo .
Z powodu łatwości pozyskiwania , stosunkowo niewielkich wymagań technologicznych podczas obróbki oraz innych zalet , drewno stanowi powszechnie wykorzystywaną grupę materiałów konstrukcyjnych w technice .
Może być ono stosowane w postaci drewna litego lub w formie przetworzonej w postaci tzw .
