Drobnin
Drobnin – wieś w Polsce położona w województwie wielkopolskim , w powiecie leszczyńskim , w gminie Krzemieniewo .
Historia .
Drobnin po raz pierwszy został odnotowany w dokumentach w 1291 roku .
Bliżej nieznani Piotrowice sprzedali wówczas Drobnin Bogumiłowi Janowicowi z rodu Leszczyców z pobliskiej Górki .
Później miejscowość wielokrotnie przechodziła z rąk do rąk .
W 1902 roku jej właścicielem został Stanisław Ponikiewski .
Do II wojny światowej majątek w Drobninie pozostawał własnością rodziny Ponikewskich .
W latach 1975−1998 miejscowość administracyjnie należała do województwa leszczyńskiego .
Edukacja .
Na terenie Drobnina znajduje się Publiczna Szkoła Podstawowa .
