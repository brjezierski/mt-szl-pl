Drohobycz ( stacja kolejowa )
Drohobycz ( ukr : `` Станція Дрогобич '' ) – stacja kolejowa w Drohobyczu , w Obwodzie lwowski , na Ukrainie .
Jest częścią Kolei Lwowskiej .
Historia .
Stacja została otwarta w dniu 31 grudnia 1872 jako część linii kolejowej Stryj - Drohobycz - Sambor - Chyrów .
Tego samego dnia została otwarta linia do Borysławia .
W 1912 roku zbudowano linię do Truskawca , co pozwoliło stać się Drohobyczowi regionalnym węzłem kolejowym .
W 1973 przeprowadzono elektryfikację stacji wraz z linią Stryj - Drohobycz - Truskawiec .
Na stacji zatrzymują się elektryczne pociągi podmiejskie i pociągi dalekobieżne .
Pociągiem elektrycznym ( Elektriczka ) można dojechać do Lwowa , Truskawca , Stryju lub Sambora .
