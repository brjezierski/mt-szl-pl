Drohomirczany
Drohomirczany ( ukr .
Драгомирчани ) – wieś w rejonie tyśmienickim Ukrainy nad Bystrzycą Sołotwińską .
Wieś prawa wołoskiego , położona była w drugiej połowie XV wieku w ziemi halickiej województwa ruskiego .
Przed 1772 miejscowość znajdowała się w granicach Rzeczypospolitej .
W latach 1772–1918 wieś znajdowała się pod zaborem austriackim i położona była na terytorium Królestwa Galicji i Lodomerii , w obwodzie stanisławowskim .
Wieś podlegała pod urząd pocztowy w Bohorodczanach oraz cerkiew prawosławną w Krechowcach i parafię rzymskokatolicką w Łyścu .
Właścicielem tabularnym był Rudolf hrabia Stadion , a współwłaścicielem Tomasz Konarski .
W 1881 wieś liczyła 814 mieszkańców , w tym 694 wiernych Cerkwi greckokatolickiej , 90 wyznawców Kościoła rzymskokatolickiego i 18 izraelitów .
24 lipca 1917 w rejonie wsi rozegrała się bitwa pod Krechowcami .
W dwudziestoleciu międzywojennym Drohomirczany były gminą wiejską w powiecie stanisławowskim w Polsce , która z dniem 1 sierpnia 1934 włączona została do gminy Łysiec .
