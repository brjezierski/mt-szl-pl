Błonka dwujądrowa
Błonka dwujądrowa ( `` Athelia binucleospora '' J. Erikss .
& amp ; Ryvarden ) – gatunek grzybów z rodziny błonkowatych ( `` Atheliaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Athelia , Atheliaceae , Atheliales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten w 1973 roku opisali John Eriksson i Leif Ryvarden na martwym drewnie olchy szarej w Szwecji .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Na podłożu tworzy cienką , błonkę o barwie , białej lub białawej , łatwo oddzielającą się od podłoża z powodu słabo rozwiniętego subikulum o konsystencji pajęczyny .
Powierzchnia błonki w stanie suchym jest gładka , w stanie świeżym często lekko pomarszczona ( nieco meruliowata ) .
Strzępki ze sprzążkami lub bez , strzępki podstawek często nieco szersze i bardziej pogrubione niż inne , w obu przypadkach często inkrustowane , nie tworzące ryzomorfów .
Podstawki dość niskie , mniej lub bardziej maczugowate z 2-4 sterygmami .
