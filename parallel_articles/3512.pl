Drołtowice
Drołtowice ( niem .
`` Rudelsdorf '' ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie oleśnickim , w gminie Syców .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kaliskiego .
Oświata .
We wsi znajduje się dom nauczyciela , a także szkoła podstawowa , w której uczą się dzieci z okolicznych wsi .
Położenie .
W okolicach wsi znajdują się źródła rzeki Widawy .
Nazwa .
W alfabetycznym spisie miejscowości na terenie Śląska wydanym w 1830 roku we Wrocławiu przez Johanna Knie wieś występuje pod polską nazwą `` Drotowice '' , zgermanizowaną `` Drotowitz '' oraz niemiecką nazwą `` Rudelsdorf '' .
Historia .
W latach 1826-1945 wieś należała do rodziny Korn z Wrocławia .
Dawniej w drołtowickim parku mieścił się zamek właścicieli , który został zburzony przez wkraczającą Armię Czerwoną .
We wsi znajdowały się także piekarnia , młyn i browar .
Są to informacje przekazane przez pierwszych mieszkańców zasiedlających wieś .
