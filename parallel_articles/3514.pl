Radziechowice Drugie
Radziechowice Drugie – wieś królewska w Polsce położona w województwie łódzkim , w powiecie radomszczańskim , w gminie Ładzice .
W latach 1975–1998 miejscowość administracyjnie należała do województwa piotrkowskiego .
W lesie , przy drodze rozpoczynającej się za cmentarzem parafialnym w Radziechowicach Drugich , znajduje się słupowa , murowana kapliczka , nawiązująca kształtem do Latarni Umarłych .
W jej okienku umieszczono obraz przedstawiający świętą Rozalię z Palermo , patronkę chroniącą przed zarazami .
Kapliczka upamiętnia znajdujący się w jej pobliżu cmentarz epidemiczny z XIX wieku .
Na terenie miejscowości znajduje się gimnazjum oraz kościół należący do Parafii rzymskokatolickiej pod wezwaniem św .
Rozalii z Palermo .
