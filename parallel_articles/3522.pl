Kopciuszek zwyczajny
Kopciuszek zwyczajny , kopciuszek ( `` Phoenicurus ochruros '' ) – gatunek małego ptaka wędrownego z rodziny muchołówkowatych ( Muscicapidae ) .
Występowanie .
Zasiedla Eurazję i północno-zachodnią Afrykę .
To ptak wędrowny ( częściowo wędrowny ) .
Osiadłe są populacje z południa Europy – Półwyspu Bałkańskiego , Włoch i Hiszpanii .
Północne wędrują na zimowiska znajdujące się w południowej Europie , północnej Afryce i od Bliskiego Wschodu do Indii i Mjanmy .
Podgatunki .
Kopciuszek zamieszkuje w zależności od podgatunku :
W 2018 roku opisany został podgatunek `` P. o. murinus '' , zasiedlający północno-wschodni Kazachstan , południowo-środkową Syberię , skrajnie północno-środkowe Chiny i zachodnią , południowo-zachodnią i zachodnio-środkową Mongolię .
Został on zaakceptowany przez IOC .
Wcześniej tę populację zaliczano do `` P. o. phoenicuroides '' .
Proponowany podgatunek `` aterrimus '' ( Portugalia , południowa i środkowa Hiszpania ) został zsynonimizowany z `` gibraltariensis '' , a `` xerophilus '' ( zachodnie Chiny ) uznano za formę przejściową między `` phoenicuroides '' i `` rufiventris '' .
Charakterystyka .
Cechy gatunku .
Ptak o smukłej sylwetce , wielkości wróbla lub nieco od niego mniejszy .
Obie płci mają podobny rozmiar i czarne nogi .
Dorosły samiec w szacie godowej jest łupkowoczarny , z czarną częścią twarzową , gardłem i piersią , białym lusterkiem na skrzydle i przejaśnieniem na podogoniu i dolnej części brzucha .
Posiada charakterystyczny , ruchliwy , rdzawy ogon z czarnymi środkowymi sterówkami .
Samica jest łupkowoszara z rdzawym ogonem i kuprem , jaśniejsza od samca .
W przeciwieństwie do samicy pleszki , samica kopciuszka ma spód ciała szary , nie kontrastujący z grzbietem ( pleszka – brzuch o odcieniu pomarańczowym i jaśniejsze gardło ) .
Tęczówki oczu są ciemnobrązowe .
Osobniki młodociane są podobne do samicy , szarobrązowe i bez białej wstawki na skrzydłach .
Samiec w swoim pierwszym sezonie lęgowym ( w drugim kalendarzowym roku życia ) może mieć czarne gardło , ale zwykle nie ma białego lusterka na lotkach .
