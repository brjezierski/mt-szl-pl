Drzonów
Drzonów ( niem .
`` Schlesisch Drehnow '' , łuż .
`` Drjenow '' ) – wieś w Polsce położona w województwie lubuskim , w powiecie zielonogórskim , w gminie Świdnica .
W latach 1975–1998 miejscowość należała administracyjnie do województwa zielonogórskiego .
Do roku 1945 wieś położona była w powiecie zielonogórskim , prowincji dolnośląskiej , w Rzeszy niemieckiej .
Parafia ewangelicka w miejscu , należała do staropruskiego związku ewangelickiego dekanatu zielonogórskiego , w diecezji opolskiej .
W dniu 9 września 1945 wieś została zasiedlona przez Polaków z Małopolski Wschodniej ( obecnie Ukraina ) , którzy w sierpniu 1945 musieli opuścić rodzinną wieś Rychcice w pow .
