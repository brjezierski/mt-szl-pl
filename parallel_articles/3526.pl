Drążewo
Drążewo – wieś w Polsce położona w województwie mazowieckim , w powiecie ciechanowskim , w gminie Sońsk .
W latach 1975–1998 miejscowość administracyjnie należała do województwa ciechanowskiego .
Wieś graniczy z miejscowościami : Sarnowa Góra , Łebki Wielkie , Mieszki , Damięty-Narwoty oraz Chrościce .
Przeważają tu głównie grunty trzeciej , czwartej i piątej klasy .
W latach 1961-2000 mieściła się tu szkoła podstawowa pod nazwą `` Berlińskiego pułku ułanów legionowych '' .
Szkoła została wybudowana przy pomocy jednostki wojskowej z Ciechanowa .
Przyszedł tutaj na świat Henryk Goryszewski .
