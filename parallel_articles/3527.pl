Du Fu
Du Fu ( 杜甫 ) , znany także jako Du Shaoling ( 杜少陵 ) lub Du Gongbu ( 杜工部 ) ( ur .
712 , zm .
770 ) – chiński poeta , tworzący w czasach dynastii Tang .
Wraz z Li Baiem uważany jest za najwybitniejszego chińskiego poetę .
Jego ojciec był średniej rangi urzędnikiem , matka zaś spokrewniona była z rodziną cesarską .
W młodości prowadził tułacze życie , następnie zgodnie z tradycją rodzinną pracował jako urzędnik na cesarskim dworze , co zawdzięczał wyłącznie protekcji cesarza , gdyż mimo trzykrotnej próby nie zdał nigdy egzaminów urzędniczych .
Po rebelii An Lushana uciekł w 757 roku ze stolicy do wygnanego cesarza .
Pisał memoriały niezbyt przychylnie przyjmowane przez władze .
Pojawiły się w tym czasie u niego także problemy zdrowotne , zachorował na gruźlicę .
W 759 roku zamieszkał w Chengdu , gdzie wybudował dom .
Był tam na służbie miejscowego dowódcy wojskowego .
Następnie przeniósł się do Guizhou , a w 768 roku ponownie wyruszył w podróż .
Zmarł prawdopodobnie w Tangzhou ( ob .
Changsha ) .
W Chengdu znajduje się rekonstrukcja słomianej chaty pod nazwą `` Du Fu Caotang '' , w której mieszkał Du Fu .
