Dua Lipa ( album )
Dua Lipa – debiutancki album studyjny brytyjskiej piosenkarki Dua Lipy .
Został on wydany 2 czerwca 2017 roku przez wytwórnię Warner Bros. Krążek zadebiutował w rodzinnym kraju artystki na 5 miejscu UK Albums Chart z łączną sprzedażą 16 223 kopii w ciągu pierwszego tygodnia .
