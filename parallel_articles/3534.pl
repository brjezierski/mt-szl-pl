Gminę zniesiono z dniem 1 października 1934 roku , a z jej obszaru utworzono nowe gminy Bielsk i Boćki ; część obszaru zniesionej gminy włączono także do gmin Kleszczele i Orla .
Demografia .
Według Pierwszego Powszechnego Spisu Ludności z 1921 roku gmina liczyła 33 miejscowości i 3499 mieszkańców ( 1804 kobiety i 1695 mężczyzn ) .
Większość mieszkańców gminy w liczbie 1897 osób zadeklarowała narodowość białoruską ( 54 % ogółu mieszkańców gminy ) .
Pozostali podali kolejno narodowość : polską ( 1569 osób ) ; żydowską ( 29 osób ) ; rosyjską ( 3 osoby ) oraz rusińską ( 1 osoba ) .
Pod względem wyznaniowym dominowali prawosławni ( 3085 osób ; 88 % wszystkich mieszkańców ) ; pozostali zadeklarowali kolejno wyznanie : rzymskokatolickie ( 344 osoby ) ; mojżeszowe ( 53 osoby ) ; adwentystyczne ( 11 osoba ) ; ewangelickie ( 2 osoby ) ; a pozostałe 4 osoby określone zostały w spisie jako „ sektanci ” .
