Dubie ( jezioro na Równinie Drawskiej )
Dubie ( także `` Adamowo '' ) – jezioro wytopiskowe znajdujące się na terenie Równiny Drawskiej , najgłębsze jezioro Puszczy Drawskiej .
Jezioro znajduje się w całości w granicach administracyjnych miasta Drawno .
Przed II wojną światową jezioro posiadało dwie niemieckie nazwy `` Großer See '' lub `` Düpsee '' .
W 1949 roku zmieniono urzędowo nazwy kilku jezior połączonych wokół miasta na jedno jezioro `` Drawno '' .
W 2006 roku Komisja Nazw Miejscowości i Obiektów Fizjograficznych w wykazie hydronimów przedstawiła dla akwenu na południe od miasta Drawna nazwę `` Dubie '' .
