Dubrownik
Dubrownik ( , , ) – miasto w Chorwacji , stolica żupanii dubrownicko-neretwiańskiej , siedziba Miasta Dubrownik .
Jest położone w południowej Dalmacji nad Morzem Adriatyckim .
Niegdyś ośrodek handlu rangi europejskiej , obecnie przede wszystkim ośrodek turystyki .
Port handlowy i pasażerski .
W 2011 roku liczyło 28 434 mieszkańców .
Dubrownik jest najchętniej odwiedzanym przez turystów miastem w Chorwacji .
W Dubrowniku jest niewiele plaż .
Miasto słynie z zabytków , architektury , lokalnej kuchni i życia nocnego .
Stare miasto w Dubrowniku – jako zespół miejski – w całości zostało wpisane na Listę Światowego Dziedzictwa Kulturowego UNESCO .
