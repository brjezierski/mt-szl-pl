Położenie .
Właściwy Dubrownik zajmuje tylko część gminy miejskiej `` Grad Dubrovnik '' .
Podane w niniejszym haśle dane o powierzchni i liczbie ludności odnoszą się do całej gminy , nie tylko do miasta .
Najbardziej znana część Dubrownika – stare miasto ( `` Stari Grad '' albo tylko `` Grad '' ) stanowi tylko niewielką część współczesnego miasta .
