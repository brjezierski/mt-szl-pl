Dudelange
Dudelange – gmina ze statusem miejskim w południowym Luksemburgu , w dystrykcie Luksemburg , w kantonie Esch-sur-Alzette .
Graniczy z Francją .
W 2014 roku , miasto Dudelange , które stanowi centrum gminy , zamieszkiwało 20 000 osób .
Inne miejscowości w gminie : Budersberg oraz Mont St. Jean .
Dudelange jest też ważnym ośrodkiem przemysłowym Luksemburga ( przemysł głównie metalowy ) .
Na terenie miasta znajduje się nadajnik radiowo-telewizyjny .
Jednym z miast partnerskich Dudelange jest Lębork .
Znajduje się tam też ulica , której patronuje polskie miasto .
W miejscowości ma siedzibę klub piłkarski F91 Dudelange .
