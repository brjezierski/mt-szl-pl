Dudley
Dudley – miasto w Wielkiej Brytanii , w Anglii w hrabstwie West Midlands , w dystrykcie Dudley na południe od Wolverhampton , będące częścią konurbacji Black Country .
Populacja miasta wynosi 194 919 mieszkańców ( dane za rok 2001 ) .
Jest drugim pod względem wielkości miastem w Anglii nieposiadającym statusu city , po Reading a przed Northampton .
Gospodarka .
W mieście rozwinął się przemysł maszynowy , metalowy , chemiczny , włókienniczy , elektronicznych , materiałów budowlanych oraz hutniczy .
