Duet
Duet – kompozycja przeznaczona dla dwóch śpiewaków lub utwór muzyczny na dwa głosy wokalne , będący samodzielnym dziełem lub częścią jednej z wielkich form muzycznych : opery , operetki , kantaty , oratorium .
Duety wokalne często mają charakter pedagogiczny , wówczas występują pod łacińską nazwą `` bicinium '' .
Potocznie używa się określenia `` duet '' ( np .
duet fortepianowy , duet gitarowy ) dla dwuosobowego zespołu instrumentalnego , w którym żaden z instrumentów nie pełni funkcji akompaniującej .
Taki zespół nosi nazwę `` duo '' .
Również trzyosobowe składy noszą odmienne określenia ( tercet – śpiewający , trio – instrumentalne ) .
Od kwartetu wzwyż nie ma rozróżnienia , określenie dotyczy zarówno czterech śpiewaków , jak i czterech instrumentalistów .
