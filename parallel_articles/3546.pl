Długie Grodzieckie
Długie Grodzieckie – wieś w Polsce , położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Ceranów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Mieszkańcy wyznania rzymskokatolickiego należą do parafii Niepokalanego Poczęcia NMP w Ceranowie .
Wieś leży w obszarze Natura 2000 w strefie ciszy , w Nadbużańskim Parku Krajobrazowym .
Wieś leży nad starorzeczem rzeki Bug , zwanym czasem Jeziorem Grodzieckim , o długości ok. 2 km oddzielonym od Bugu wałem przeciwpowodziowym .
Wieś ma charakter rolniczo-turystyczny .
Nad jeziorem większość domów to domy letniskowe .
