Długi Kąt ( przystanek kolejowy )
Długi Kąt – przystanek osobowy w Tartaku Długim Kącie , w gminie Józefów , w powiecie biłgorajskim , w województwie lubelskim , w Polsce .
W okresie od 28 czerwca do 1 września przez przystanek kursowały autobusy szynowe uruchomione przez spółkę Przewozy Regionalne .
Jeździły one na trasie z Lublina do Jarosławia oraz z Lublina do Bełżca .
