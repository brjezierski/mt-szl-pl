Duino-Aurisina
Duino-Aurisina ( słow .
`` Devin-Nabrežina '' ) – miejscowość i gmina we Włoszech , w regionie Friuli-Wenecja Julijska , w prowincji Triest .
Według danych na rok 2004 gminę zamieszkiwały 8753 osoby , 194,5 os./km² .
