Duisburg
Duisburg – miasto na prawach powiatu położone w zachodniej części Niemiec , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf , w Zagłębiu Ruhry ( `` Ruhrgebiet '' ) .
Miasto leży nad Renem i jest dużym ośrodkiem przemysłowym .
Historia .
Duisburg uzyskał prawa miejskie w 1279 roku .
Miasto należało w średniowieczu do Hanzy , czyli do związku miast handlowych Europy Północnej z czasów średniowiecza i początku ery nowożytnej .
