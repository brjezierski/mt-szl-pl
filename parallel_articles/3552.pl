Love Parade .
W Duisburgu 24 lipca 2010 roku odbył się największy plenerowy festiwal muzyki elektronicznej Love Parade odbywający się w Niemczech od 1989 roku .
Jedynym wejściem i wyjściem był tunel na stary dworzec towarowy , w którym mogło zmieścić się jedynie 250 tys .
osób , przy czym na paradę przybyło aż 1,4 miliona osób .
W wyniku chaosu , który powstał z powodu blokady ruchu przez policję , w tunelu tym wybuchła panika , w której zginęło 21 osób , a ponad 340–500 zostało rannych .
Po tym zdarzeniu ogłoszono , że Love Parade nigdy więcej się nie odbędzie .
