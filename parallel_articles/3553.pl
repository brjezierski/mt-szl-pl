Dukinfield
Dukinfield – mała miejscowość położona w angielskim hrabstwie Wielki Manchester , w okręgu Tameside .
Miejscowość nabyła prawa miejskie w okresie Rewolucji przemysłowej i gwałtownego wzrostu angielskiego przemysłu włókienniczego , bazującego na bawełnie .
Produkowano w nim części do maszyn włókienniczych .
Obecnie miasto ma charakter mieszkalny , nie posiada przemysłu .
Historia .
W XII wieku , po inwazji Normanów części ziem opanowanych przez dwie rodziny najeźdźców nadano nazwę `` De Dokenfeld '' .
Obecna nazwa miasta wywodzi się od pułkownika Roberta Duckenfielda , który w okresie Angielskiej wojny domowej mianowany został komendantem New Model Army .
W Dukinfield urodziła się aktorka Shirley Stelfox
