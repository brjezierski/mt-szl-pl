Dumitru Hubert
Dumitru Hubert ( ur .
3 września 1899 w Bukareszcie , zm .
27 sierpnia 1934 w Braszowie ) – rumuński bobsleista , uczestnik Zimowych Igrzysk Olimpijskich w Lake Placid w 1932 , pilot wojskowy .
Bobsleje .
Igrzyska olimpijskie .
Dumitru Hubert uczestniczył w Zimowych Igrzyskach Olimpijskich w 1932 w położonym w Stanach Zjednoczonych Lake Placid .
Reprezentacja Rumunii , składająca się wyłącznie z bobsleistów , pojechała na tę imprezę dzięki dofinansowaniu z Ministerstwa Wojny .
Na olimpiadzie wystąpił zarówno w dwójce jak i w czwórce .
W parze z Alexandru Papanem zajął 4. miejsce na 12 startujących .
W czwórce reprezentacja Rumunii w składzie Alexandru Papană , Alexandru Ionescu , Ulise Petrescu , Dumitru Hubert zajęła przedostatnie , 6. miejsce .
Obie konkurencje wygrali reprezentanci gospodarzy
Były to jedyne igrzyska olimpijskie w karierze tego bobsleisty .
Mistrzostwa świata .
Rok po igrzyskach Dumitru Hubert wraz z Alexandru Papanem zdobył mistrzostwo świata w bobslejowej dwójce w Schreiberhau w Niemczech ( obecnie Szklarska Poręba ) .
W 1934 ekipa ta zdobyła brązowy medal .
Śmierć .
Dumitru Hubert zginął 27 sierpnia 1934 roku w katastrofie podczas pokazów lotniczych w Braszowie .
