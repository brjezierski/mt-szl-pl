Demografia .
W 2001 liczyło 23 519 mieszkańców , w tym 18 756 Węgrów ( 79,75 % ) , 3588 Słowaków , 353 Romów , 147 Czechów i 24 Niemców .
Z prawie 80 % populacją węgierską jest pod tym względem trzecim miastem na Słowacji , jest również jednym z miast najbardziej rozwijających się .
Gospodarka .
W mieście rozwinął się przemysł spożywczy , włókienniczy oraz maszynowy .
