Historia .
Pierwsza wzmianka pochodzi z około 1250 – miejscowość nazywała się wówczas `` Zerda '' .
Później nazwa zmieniała się jeszcze kilkukrotnie – `` Svridahel '' ( 1254 ) , `` Zeredahely '' ( 1270 ) , `` Zerdahel '' ( 1283 ) , `` Zredahel '' ( 1358 ) .
Dopiero w XVIII wieku upowszechniła się nazwa `` Szerdahely '' , która w wolnym tłumaczeniu „ Miejsce środowych targów ” lub „ Środowe miasto ” ( miejscowość otrzymała przywilej urządzania targów w środy , chociaż później przeniesiono je na piątek ) .
Związek z targiem widoczny też jest w niemieckiej nazwie , która oznacza „ Dolny targ ” .
W II połowie XIX wieku dodano przedrostek `` Duna- '' ( Dunajski ) ; nazwa słowacka jest używana od 1919 .
Przez większość swojego istnienia Dunajská Streda była niewielkim miasteczkiem , pozostającym w cieniu Pozsony ( dzisiejszej Bratysławy ) .
Położone było w południowej części komitatu Pozsony ( `` Pozsony vármegye '' ) Królestwa Węgier .
Od 1574 miasto królewskie , a od 1600 do 1848 należała do rodziny Pálffy .
W XVIII wieku zaczęła się tutaj osiedlać liczna społeczność żydowska ( z tego też powodu w XIX wieku nazywano ją Małą Jerozolimą ) .
W 1880 na 4182 mieszkańców 3531 posługiwało się językiem węgierskim , a 416 niemieckim ( spis nie wyróżniał narodowości , lecz używane języki , przy czym liczebność społeczności żydowskiej szacowano na 1,5-2 tysiące ) .
W 1910 w spisie 4679 zadeklarowało język węgierski jako ojczysty , na ogólną liczbę 4762 .
Po traktacie w Trianon miasto znalazło się w granicach Czechosłowacji .
Spis z 1930 podawał , że na 5706 mieszkańców 2944 było Węgrami , 2186 Żydami ( głównie węgierskojęzycznymi ) , 503 Słowakami , a 73 Niemcami .
Na krótko , w latach 1938–1945 powróciło do Węgier .
Pod koniec II wojny światowej społeczność żydowska została przewieziona do obozów koncentracyjnych i wymordowana , natomiast synagoga uległa zniszczeniu podczas bombardowań , a ruiny usunięto w latach 50 .
W okresie socjalizmu miejscowość poddano szybkiej industrializacji .
W latach 90. zaniedbane centrum miasta poddano spektakularnej przebudowie – węgierski architekt Imre Makovecz odbudował ( a właściwie wybudował od nowa ) starówkę .
