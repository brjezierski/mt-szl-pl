Duque de Caxias
Duque de Caxias ( wym . )
– miasto w południowo-wschodniej Brazylii , w stanie Rio de Janeiro , trzecie co do wielkości w okręgu metropolitalnym Rio de Janeiro .
Zostało nazwane na cześć urodzonego tam Luísa Alves de Lima e Silva , księcia Caxias .
W mieście rozwinął się przemysł samochodowy , rafineryjny , spożywczy , włókienniczy oraz cementowy .
