Durrës ( 1926 )
Durrës – albański patrolowiec z okresu międzywojennego , jedna z czterech jednostek typu `` Tirana '' .
Została zbudowana w 1926 roku we włoskiej stoczni Società Veneziana Automobili Navali w Wenecji i w tym samym roku przyjęto ją do służby w Marynarce Wojennej Albanii .
Okręt został zdobyty przez Włochów w 1939 roku , a po zawarciu przez Włochy zawieszenia broni z Aliantami w 1943 roku został przejęty przez Kriegsmarine .
Jednostka po wojnie powróciła pod banderę albańską i służyła prawdopodobnie do lat 70 .
