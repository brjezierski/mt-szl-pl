Duszniki-Zdrój ( stacja kolejowa )
Duszniki-Zdrój – stacja kolejowa w Dusznikach-Zdroju , w powiecie kłodzkim , w województwie dolnośląskim , w Polsce .
W roku 2017 stacja obsługiwała 100–149 pasażerów na dobę .
Lokalizacja .
Stacja kolejowa położona na linii kolejowej z Kłodzka do Kudowy-Zdroju .
Znajduje się na niezelektryfikowanej trasie o znaczeniu lokalnym , w Dusznikach Zdroju w północno-zachodniej części miasta , na granicy zwartej zabudowy .
Historia .
Historia projektu linii kolejowej do Dusznik-Zdroju sięga 2 połowy XIX w. , kiedy powstawały pierwsze opracowania na ten temat .
Wielkim ich orędownikiem był ówczesny burmistrz miasta Paul Dengler .
W latach 1886–1890 rozpoczęto realizację planu budowy `` Kolei Uzdrowiskowej '' .
Zakończono jednak jego realizację na Szczytnej .
Władze miasta wystosowały wobec tego pismo do władz państwowych z prośbą o przedłużenie trasy aż do granicy państwa , tj .
Kudowy-Zdroju .
Ostatecznie zdecydowano odłożyć pracę na późniejszy termin mimo przekazania przez miasto bezpłatnie terenów pod budowę torów .
Wobec tego w latach 1895–1896 powstała inicjatywa połączenia szynowego ze Szczytnej do Dusznik jako prywatnej kolejki elektrycznej , jednak pomysł ten nie został nigdy zrealizowany .
Przełom nastąpił na początku XX w. , władze zdecydowały o rozbudowie linii .
Od grudnia 1901 r. rozpoczęto prace przy pomocy materiałów wybuchowych .
Dworce ( osobowy i towarowy ) usytuowano na wysokości 554 m n.p.m .
Przy budowie brali udział robotnicy różnych narodowości , w tym Polacy , Czesi , Włosi i Chorwaci .
30 listopada 1902 miał miejsce pierwszy próbny przejazd do miasta , zaś od 15 grudnia zaczęły kursować normalne pociągi .
Rozwijający się ruch turystyczny spowodował , że kilka lat później , w 1906 r. w miejscu poprzedniego parterowego zbudowano nowy dworzec w stylu Jugendstil .
W 1928 z inicjatywy ówczesnego burmistrza Dusznik Zdroju dr. Josepha Goebela zmieniono nazwę stacji na `` Bad Reinerz '' .
Po II wojnie światowej i przejściu ziemi kłodzkiej pod administrację polską zmieniono nazwę dworca na `` Duszniki Zdrój '' .
