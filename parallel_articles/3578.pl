Prace Duże
Prace Duże – wieś w Polsce , położona w województwie mazowieckim , w powiecie piaseczyńskim , w gminie Tarczyn .
Częścią sołectwa Prace Duże jest Leśna Polana .
W latach 1975–1998 miejscowość administracyjnie należała do województwa warszawskiego .
Dwór w Pracach Dużych .
We wsi znajduje się dwór murowany z cegły .
Zespół dworski powstał w 2 połowie XIX wieku , jednak obecny dwór wybudowano w dwudziestoleciu międzywojennym .
W latach 30 .
XX wieku należał do Apolinarego Budnego , później do Romany Orłowskiej .
Pozostawał w ruinie do lat 90. , obecnie odbudowany , jest własnością prywatną .
