Klonownica Duża
Klonownica Duża – wieś w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Rokitno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
Miejscowość jest siedzibą rzymskokatolickiej parafii św .
Jana Apostoła .
Zabytkowy kościół parafialny św .
Jana Ewangelisty , został wzniesiony w latach 1854–1861 jako cerkiew unicka .
Po likwidacji unickiej diecezji chełmskiej cerkiew została przekształcona w świątynię prawosławną , a następnie rewindykowana na kościół w 1918 .
Na południowy zachód od wsi znajduje się zabytkowy cmentarz prawosławny w Klonownicy Dużej , obecnie niegrzebalny .
