Wólka Duża
Wólka Duża ( dawn .
Wólka Goła ) – wieś w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Stanisława Biskupa i Męczennika w Dąbrowie Wielkiej .
Historia .
Jeszcze w 1. połowie XX w. `` Wólka Goła '' .
W roku 1827 miejscowość liczyła 20 domów i 126. mieszkańców .
Pod koniec XIX w. wieś drobnoszlachecka w powiecie `` mazowieckim '' , gmina Szepietowo , parafia Dąbrowa Wielka .
W roku 1921 w `` Wólce Gołej '' było 23 budynki z przeznaczeniem mieszkalnym i 145. mieszkańców ( 70. mężczyzn i 75 kobiet ) .
Wszyscy podali narodowość polską i wyznanie rzymskokatolickie .
