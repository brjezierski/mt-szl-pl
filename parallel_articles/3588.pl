Świeciechów Duży
Świeciechów Duży – wieś w Polsce położona w województwie lubelskim , w powiecie kraśnickim , w gminie Annopol .
Leży przy drodze wojewódzkiej 824 .
Za Królestwa Polskiego istniała gmina Świeciechów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnobrzeskiego .
W Świeciechowie znajduje się parafialny kościół polskokatolicki pw .
Świętego Jana Chrzciciela .
W 1946 odznaczona Orderem Krzyża Grunwaldu III klasy .
Wieś stanowi sołectwo gminy Annopol .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 565 mieszkańców .
