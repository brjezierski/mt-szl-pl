Żeliszew Duży
Żeliszew Duży – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Kotuń .
Do 1954 roku istniała gmina Żeliszew .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Na terenie wsi znajduje się parafialny mariawicki kościół pw .
Wniebowzięcia Najświętszej Maryi Panny .
W miejscowości działa założona w 1909 roku jednostka ochotniczej straży pożarnej .
W 2018 roku straż otrzymała średni samochód ratowniczo-gaśniczy GBA 3,5/27/4,7 Iveco EuroCargo .
W miejscowości swoją siedzibę ma klub piłkarski Jastrząb Żeliszew .
