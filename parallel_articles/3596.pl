Rapy Dylańskie
Rapy Dylańskie – kolonia w Polsce położona w województwie lubelskim , w powiecie biłgorajskim , w gminie Biłgoraj .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 99 mieszkańców i była 26. co do wielkości miejscowością gminy Biłgoraj .
Wieś jest otoczona lasami Puszczy Solskiej i leży ok. 4km na północ od Biłgoraja .
Dobiega do niej droga powiatowa nr 2917L , łącząca ją z dzielnicą Rapy w Biłgoraju .
W pobliżu miejscowości , przy wspomnianej drodze , znajduje się leśny zbiornik retencyjny na rzece Osie o głębokości ok. 2m i powierzchni 1,93ha .
Historia .
W 1827 roku Rapy Dylańskie liczyły 8 domostw i 91 mieszkańców wchodząc w skład dóbr Dyle .
