Dębica ( stacja kolejowa )
Dębica – węzłowa stacja kolejowa w Dębicy , w województwie podkarpackim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca regionalnego .
Obsługiwana jest z nastawni „ Db ” za pomocą komputerowych urządzeń sterowania ruchem kolejowym .
Stacja ma 2 wyspowe perony .
W roku 2018 stacja obsługiwała ok. 1500 pasażerów na dobę .
Historia .
Starania o budowę kolei żelaznej z Krakowa przez Dębicę do Lwowa rozpoczęły się już w 1837 , ale z uwagi na brak finansów robót nie rozpoczęto do 1850 .
Uprzywilejowana Kolej Żelazna Północna Cesarza Ferdynanda rozpoczęła przygotowania do budowy linii , a do 1853 zbudowany został odcinek Dębica – Bochnia .
Nie był on jednak użytkowany , dopiero w 1854 rozpoczęto budowę brakującego odcinka Bochnia – Kraków .
15 października 1855. przyjechał do Dębicy pierwszy pociąg , oficjalne otwarcie nastąpiło dopiero 20 lutego 1856 .
Kursowały wtedy dwa pociągi dziennie .
