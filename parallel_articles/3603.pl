Dęblin ( stacja kolejowa )
Dęblin – stacja kolejowa w Dęblinie , w województwie lubelskim , w Polsce .
Położony jest przy ulicy Dworcowej 6 , w odległości około 1,5 km na północny zachód od centrum miasta .
Stacja ma 3 perony .
Zatrzymują się tu pociągi dalekobieżne i regionalne .
Jest to stacja węzłowa .
Znajdują się tu również automaty biletowe .
1 maja 1916 zmieniono nazwę stacji Iwangród na Dęblin .
W roku 2018 stacja obsługiwała ok. 2200 pasażerów na dobę .
