Dębno ( jezioro w Dolinie Gwdy )
Dębno ( niem .
`` Gr .
Damen See '' ) – jezioro rynnowe w Polsce położone w województwie zachodniopomorskim , w powiecie szczecineckim , w gminie Szczecinek .
Akwen leży na Pojezierzu Drawskim , otoczony jest lasami .
Około 2500 metrów na północ od jeziora znajduje się miejscowość Drężno .
