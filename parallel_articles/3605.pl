Dębogóra ( Pojezierze Kaszubskie )
Dębogóra – wzniesienie o wysokości 92,2 m n.p.m. , znajduje się na Pojezierzu Kaszubskim i położone jest w woj .
pomorskim , w powiecie lęborskim , na obszarze administracyjnym miasta Lęborka .
Ok. 1,5 km na północ od wzniesienia znajduje się centrum miasta .
Do 1945 r. stosowano niemiecką nazwą `` Eck Berg '' .
W 1949 r. ustalono urzędowo polską nazwę `` Dębogóra '' .
