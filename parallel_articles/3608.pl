Demokratyczna Republika Konga
Demokratyczna Republika Konga ( ) – drugie pod względem powierzchni , trzecie pod względem liczby ludności państwo Afryki , położone w jej środkowej części , w dorzeczu rzeki Kongo , u której ujścia posiada 37 km dostępu do Oceanu Atlantyckiego .
Nazwa , oznaczająca `` myśliwy '' , pochodzi od ludu Bakongo .
W okresie kolonialnym DR Konga występowała jako Wolne Państwo Kongo ( do 1908 ) i Kongo Belgijskie ( do 1960 ) , a po uzyskaniu niepodległości w latach 1971–1997 jako Zair .
Dla odróżnienia od sąsiadującej Republiki Konga ( `` Kongo , Kongo-Brazzaville '' ) , DR Konga występowała również jako `` Kongo-Léopoldville '' , a obecnie po zmianie nazwy stolicy , jako `` Kongo-Kinszasa '' .
Największe miasta kraju : Kinszasa , Lubumbashi , Mbuji-Mayi , Kisangani , Kananga .
Kraj zamieszkuje 250 grup etnicznych .
Największe to Kongo , Luba i Mongo .
Etymologia .
Nazwa kraju pochodzi od rzeki Kongo , której większa część przepływa przez terytorium kraju .
Nazwa rzeki pochodzi zaś od nazwy jednego z ludów Bantu , Bakongo ( co znaczy myśliwi ) , które zamieszkiwało ujście rzeki , gdy zostało ono odkryte przez Portugalczyków w XV wieku .
Geografia .
Demokratyczna Republika Konga jest 11. największym państwem na świecie pod względem powierzchni .
Ukształtowanie powierzchni .
Dżungla pokrywa większość rozległej , nisko położonej kotliny , która obniża się w kierunku Atlantyku na zachodzie .
Obszar ten otoczony płaskowyżami łączy się z sawannami na południu i południowym zachodzie , górami na zachodzie i stepami przekraczającymi na północy rzekę Kongo .
Wysokie góry są na wschodnim krańcu kraju .
Klimat .
Dominuje klimat tropikalny .
W równikowej zlewni rzeki Kongo jest gorący i wilgotny .
Chłodniejszy i suchszy – na wyżej położonych terenach południowych .
Chłodniejszy i wilgotniejszy na górskich terenach na wschodzie .
Na północy od równika pora wilgotna trwa od kwietnia do października , a sucha od grudnia do lutego .
Na południe od równika : pora wilgotna trwa od listopada do marca , a sucha od kwietnia do października .
