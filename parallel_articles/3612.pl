Dynów ( stacja kolejowa )
Dynów – wąskotorowa stacja kolejowa Przeworskiej Kolei Dojazdowej w Dynowie , w województwie podkarpackim , w Polsce .
Jest to stacja końcowa PKD .
Została otwarta 8 września 1904 roku razem z wąskotorową linią kolejową z Przeworska do Dynowa .
W sezonie letnim obsługuje ruch turystyczny .
Na jednym z torów stoi jako pomnik parowóz Px48-1734 z dwoma wagonami towarowymi .
