Odległość Levenshteina
Odległość Levenshteina ( edycyjna ) – miara odmienności napisów ( skończonych ciągów znaków ) , zaproponowana w 1965 roku przez Władimira Lewensztejna .
Definicja .
Formalnie jest to metryka w przestrzeni ciągów znaków , zdefiniowana następująco :
Miara ta znajduje zastosowanie w przetwarzaniu informacji , danych w postaci ciągów symboli : w maszynowym rozpoznawaniu mowy , analizie DNA , rozpoznawaniu plagiatów , korekcie pisowni ( np .
wyszukiwanie w spisie telefonicznym błędnie podanego nazwiska ) itp .
