Dzierżysławice
Dzierżysławice ( dodatkowa nazwa w j. niem .
Dirschelwitz , ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
Przepływa przez nią rzeka Osobłoga .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 376 osób .
Nieoficjalną częścią wsi jest Kolonia .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 7 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy powiatu prudnickiego z powiatem głubczyckim ( gmina Głubczyce ) .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Przez granice administracyjne wsi przepływa rzeka Osobłoga .
