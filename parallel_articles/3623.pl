Dzwonek ( źródło )
Dzwonek – obfite źródło w prawej ( wschodniej ) odnodze Doliny nad Żbikiem ( zwanej też doliną Dzwonek ) na Wyżynie Olkuskiej w północnej części należącego do Krzeszowic osiedla Żbik .
Źródło znajduje się w Parku Krajobrazowym Dolinki Krakowskie , z niego rozpoczyna bieg potok Dzwonek , który wpada lewobrzeżnie do Krzeszówki .
