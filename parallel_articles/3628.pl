Düsseldorf
Düsseldorf [ ] – miasto na prawach powiatu w Niemczech , stolica kraju związkowego Nadrenia Północna-Westfalia oraz rejencji Düsseldorf .
Należy do aglomeracji `` Rhein-Ruhr '' z ok. 10–12 mln mieszkańcami .
Leży na wysokości 38 m n.p.m .
nad Renem , powierzchnia 217,21 km² .
Jest to jedno z większych miast zachodnich Niemiec , o dużym znaczeniu gospodarczym .
Düsseldorf , który po II wojnie światowej przeżył fazę przyśpieszonego rozwoju , jest siedzibą wielu niemieckich koncernów przemysłowych .
Pod koniec 2019 roku , z liczbą mieszkańców wynoszącą około 620 tys. , miasto zajmowało siódme miejsce w Niemczech pod względem liczby ludności .
Historia .
Pierwsza wzmianka historyczna o miejscowości jako `` Dusseldorp '' pochodzi z 1135 roku .
Prawa miejskie zostały nadane w 1288 roku .
W 1380 Düsseldorf stał się siedzibą władców księstwa Bergu , a w 1815 stał się częścią państwa pruskiego i stolicą rejencji Düsseldorf .
W 1946 roku miasto stało się stolicą nowo utworzonego kraju związkowego Nadrenia Północna-Westfalia .
Środowisko naturalne .
Düsseldorf jest położony przy dolnej części Renu , przy delcie rzeki Düssel , która wpływa do Renu .
Poza okręgiem administracyjnym 4 , reszta miasta położona jest na prawym brzegu Renu .
Po drugiej stronie Renu , na delcie rzeki Erft zbudowano miasto Neuss .
Düsseldorf jest położony na południowy zachód od Zagłębia Ruhry .
