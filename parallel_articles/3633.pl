Obwód dniepropetrowski
Obwód dniepropetrowski ( ) – jeden z 24 obwodów Ukrainy .
Leży w południowo-wschodniej części Ukrainy , nad Dnieprem .
Stolicą obwodu jest miasto Dniepr .
Obwód graniczy na północy z obwodem połtawskim i charkowskim , na wschodzie z donieckim , na południu z zaporoskim i chersońskim , na zachodzie z mikołajowskim i kirowohradzkim .
Obwód w całości leży na obszarze historycznego Zaporoża .
Za panowania Jagiellonów obszar stanowił część Wielkiego Księstwa Litewskiego .
W 1569 został włączony do Korony Królestwa Polskiego .
Stanowił wówczas południową część województwa kijowskiego .
Został utracony w 1667 na mocy postanowień rozejmu andruszowskiego , a utratę potwierdził traktat Grzymułtowskiego w 1686 r .
W 1635 roku z inicjatywy hetmana wielkiego koronnego Stanisława Koniecpolskiego wzniesiono tu twierdzę Kudak , główną polską twierdzę na Zaporożu .
Udział w rozwoju wydobycia i przemysłu w regionie w XIX w. mieli Polacy , np .
polski geolog Stanisław Kontkiewicz prowadził badania złóż rud żelaza w okolicach Krzywego Rogu , a polski przedsiębiorca Ignacy Jasiukowicz budował i prowadził huty w Kamieńskiem .
Ignacy Jasiukowicz jest upamiętniony w Kamieńskiem pomnikiem .
W obwodzie rozwinął się przemysł hutniczy , metalowy , maszynowy oraz chemiczny .
W regionie uprawia się zboże oraz hoduje się bydło oraz trzodę chlewną .
