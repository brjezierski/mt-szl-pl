Dukszty ( stacja kolejowa )
Dukszty ( , ) – stacja kolejowa w miejscowości Dukszty , w rejonie ignalińskim , na Litwie .
Leży na linii dawnej Kolei Warszawsko-Petersburskiej .
Stacja powstała w XIX w. pomiędzy stacjami Ignalin a Nowoaleksandrowsk .
Dawniej od stacji odchodziła obecnie rozebrana linia wąskotorowa Dukszty – Druja .
Od stacji odchodzi bocznica do Ignalińskiej Elektrowni Jądrowej .
