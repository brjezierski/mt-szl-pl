Domachowo ( województwo pomorskie )
Domachowo ( kaszb .
`` Domôchòwò '' ) – osada w Polsce położona w województwie pomorskim , w powiecie gdańskim , w gminie Trąbki Wielkie .
Miejscowość położona przy drodze wojewódzkiej nr 226 , stanowi sołectwo gminy Trąbki Wielkie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Historia .
Wieś a obecnie osada , notowana w dokumentach źródłowych od roku 1389 jako von Domach .
Pod nazwą Domachowo występuje w dokumentach w roku 1534 .
Słownik geograficzny Królestwa Polskiego z roku 1880 wymienia wieś Domachowo alias `` Dommachau '' , jako stare dobra rycerskie w powiecie gdańskim , parafii Pręgowo .
W drugiej połowie XIX wieku ziemi ornej było 1077 mórg .
Ludność według wyznań : katolików 36 , ewangelików 38 , Domów mieszkalnych w liczbie 4 budynki .
W roku 1650 posiadaczem był Hertmański , w roku 1766 Prądzyński , asesor tucholski , w roku 1789 Kazimierz Zboiński .
