Dąb Katowice ( hokej na lodzie )
Dąb Katowice – polski klub hokeja na lodzie z siedzibą w Katowicach , będący sekcją sportową GKS Dąb Katowice .
Klub został rozwiązany .
W 1938 trenerem zespołu został 22-letni Kanadyjczyk Frank Ney , polecony przez Bunny'ego Ahearne i pozyskany z angielskiej drużyny Wembley Stars ( w której występował Polak Jan Grabowski ) .
Ney był także zawodnikiem Dębu .
