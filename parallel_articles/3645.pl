Dąbrowa ( sielsowiet Hoża )
Dąbrowa ( biał .
`` Дуброва '' , ros .
`` Дуброва '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Hoża , nad Niemnem , który stanowi tu granicę państwowa z Litwą .
Historia .
W latach 1921–1939 wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Porzecze .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 57 osób , 56 było wyznania rzymskokatolickiego a 1 prawosławnego .
Jednocześnie 9 mieszkańców zadeklarowało polską przynależność narodową , 46 białoruską a 2 inną .
Było tu 9 budynków mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w Przewałce .
Podlegała pod Sąd Grodzki w Druskiennikach i Okręgowy w Grodnie ; właściwy urząd pocztowy mieścił się w Porzeczu .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
4 grudnia 1939 włączona do nowo utworzonego obwodu białostockiego .
Od czerwca 1941 roku pod okupacją niemiecką .
22 lipca 1941 r. włączona w skład okręgu białostockiego III Rzeszy .
W 1944 miejscowość została ponownie zajęta przez wojska sowieckie i włączona do obwodu grodzieńskiego Białoruskiej SRR .
