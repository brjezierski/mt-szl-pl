Dąbrówka-Stany
Dąbrówka-Stany – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Skórzec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W Dąbrówce-Stanach znajduje się kaplica Kościoła Starokatolickiego Mariawitów pochodząca z 1915 , która należy do parafii Wniebowzięcia Najświętszej Maryi Panny w Żeliszewie Dużym .
Znajdują się tam dwa sklepy spożywcze , remiza strażacka , świetlica wiejska i Szkoła Podstawowa gruntownie przebudowana w latach 2018-2020 .
We wsi działa założona w 1917 jednostka Ochotniczej Straży Pożarnej .
Posiada wóz bojowy GBA 2,5/16 Star 244 .
W 2020 roku strażacy pozyskali fabrycznie nowy samochód ratowniczo-gaśniczy GBA 3,5/27 Iveco EuroCargo .
Nazwa .
Nazwa `` Dąbrówka '' pojawia się w XV-wiecznych `` Rocznikach '' Długosza .
Kronikarz wspomina , że z lasu obok tej wsi wypływa rzeka Kostrzyń , dopływ Liwca .
W istocie źródła tej rzeki znajdują się dalej na południowy wschód - niedaleko Domanic .
Nazwa `` Stany '' może nawiązywać do istnienia we wsi w średniowieczu postoju dla koni .
Być może ów postój służył podróżnym w drodze do odległego o około 20 km grodu w pobliżu współczesnej wsi Grodzisk .
Ponieważ gród ten powstał ( i został zniszczony ) w XI wieku , możliwe że wówczas powstały również Stany .
