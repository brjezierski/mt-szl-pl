Dąbrówka-Ług
Dąbrówka-Ług – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Skórzec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Ochotnicza Straż Pożarna .
Ochotniczą Straż Pożarną powołano we wsi 22 maja 1920 .
Początkowo dysponowała ręczną pompą i dwoma konnymi beczkowozami o pojemności 200 litrów każdy .
W późniejszych latach zakupiono specjalistyczny konny wóz strażacki i wybudowano remizę .
Od 1967 jednostka dysponowała samochodem strażackim ( pozyskanym jako złom i przystosowanym do potrzeb OSP ) .
W latach 80 .
XX wieku jednostka otrzymała dwa specjalistyczne samochody strażackie .
Od 1995 OSP w Dąbrówce-Ług należy do Krajowego Systemu Ratowniczo-Gaśniczego .
W 2020 roku jednostka używa dwóch wozów : Scanii GBA 3/32 i Jelcza GCBA 6/32 .
