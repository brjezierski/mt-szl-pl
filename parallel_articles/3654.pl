Dominika
Dominika , Wspólnota Dominiki ( , `` Commonwealth of Dominica '' ) – państwo na wyspie o takiej samej nazwie na Morzu Karaibskim w grupie Wysp Nawietrznych ( Małe Antyle ) , pomiędzy Gwadelupą i Martyniką .
Warunki naturalne .
Dominika jest górzystą wyspą pochodzenia wulkanicznego .
Prawie cała wyspa , z wyjątkiem nizin nadbrzeżnych , pokryta jest pasmami górskimi z najwyższym szczytem Morne Diablotins ( 1447 m n.p.m . ) .
Na wyspie panuje klimat podrównikowy , bardzo wilgotny , z występującymi dość często cyklonami tropikalnymi .
Średnia temperatura powietrza utrzymuje się na stałym poziomie przez cały rok i wynosi od 19 do 32 °C , roczna suma opadów notowana w Roseau wynosi 1980 mm .
Dominującą roślinnością kraju jest wilgotny las równikowy .
Wyspa posiada rozwiniętą sieć rzeczną w postaci licznych rzek i potoków górskich , występują również liczne źródła mineralne .
Bogaty świat zwierzęcy i roślinny obfituje w liczne gatunki ptaków oraz kwiatów , w tym w narodowy kwiat Dominiki – Bwa Carib .
Ustrój polityczny .
Dominika jest republiką , członkiem Wspólnoty Narodów .
Aktualnie obowiązująca konstytucja z 1978 roku została uchwalona w roku uzyskania pełnej niepodległości .
Głową państwa jest prezydent , wybierany przez parlament na 5-letnią kadencję ( maksymalnie dwie kadencje ) .
