Gospodarka .
Główne produkty przemysłu rolno-spożywczego to cukier , tytoń oraz kawa , które są obok złota i innych metali cennym towarem eksportowym .
Bardzo dużą rolę odgrywa przynosząca znaczne dochody turystyka .
Dominikana posiada najlepiej rozwiniętą na Karaibach infrastrukturę pod kątem turystów .
Dwoma najbardziej znanymi regionami wypoczynkowymi są Punta Cana i Puerto Plata .
W obydwu dominują wielkie kompleksy hotelowe międzynarodowych sieci , usytuowane bezpośrednio przy plażach uważanych za jedne z najpiękniejszych na Karaibach .
Emisja gazów cieplarnianych .
Emisja równoważnika dwutlenku węgla z Dominikany wyniosła w 1990 roku 16,603 Mt , z czego mniej więcej połowę stanowił dwutlenek węgla , nieco mniej emisje metanu i znacząco mniej podtlenku azotu .
W przeliczeniu na mieszkańca emisja wyniosła wówczas 1,116 t dwutlenku węgla , a w przeliczeniu na 1000 dolarów PKB 204 kg .
Od tego czasu emisje dwutlenku węgla rosną nadal praktycznie jednostajnie ( z większym skokiem w 2002 , a po nim dwuletnim spadkiem ) , a pozostałych gazów cieplarnianych pozostają na podobnym poziomie .
Głównym źródłem emisji przez cały czas była energetyka , a transport był na drugim miejscu .
W 2018 emisja dwutlenku węgla pochodzenia kopalnego wyniosła 25,177 Mt , a w przeliczeniu na mieszkańca 2,313 t i w przeliczeniu na 1000 dolarów PKB 150 kg .
