Dominikana
Dominikana , oficjalna nazwa : Republika Dominikańska – drugie co do wielkości ( po Kubie ) państwo na Morzu Karaibskim .
Zajmuje około 2/3 powierzchni wyspy Haiti , dzieląc ją z położoną po zachodniej stronie Republiką Haiti .
Geografia .
Cyklony tropikalne sieją spustoszenie w kraju średnio co dwa lata .
Historia .
Prekolumbijskimi mieszkańcami kraju byli Karaibowie i Tainowie .
W 1492 obszar został odkryty dla Europy przez Krzysztofa Kolumba .
Dominikana pod nazwą Hispaniola weszła w skład hiszpańskiego imperium kolonialnego .
W 1697 obszar dzisiejszej Dominikany stał się odrębną kolonią hiszpańską pod nazwą Santo Domingo .
W tym samym roku zachodnia część wyspy trafiła pod władanie francuskie .
W latach 1795–1808 cała wyspa znalazła się pod rządami Francji .
W 1809 rządy nad Santo Domingo przywrócono Hiszpanom .
W 1821 Dominikana ogłosiła niepodległość , jednak już w 1822 została zajęta przez wojska Haiti ( niepodległego państwa powstałego na zachodzie ) .
W 1844 proklamowana została niepodległa Republika Dominikany .
Po kilku kolejnych próbach aneksji Dominikany przez Haiti rząd republiki zaproponował Hiszpanii ponowne włączenie w skład imperium .
Hiszpanie zaakceptowali ofertę i w 1861 Dominikana znów stała się kolonią .
W latach 1863–1865 trwało antyhiszpańskie powstanie zakończone ponownym ogłoszeniem niepodległości .
W kolejnych latach nasilił się chaos i wewnętrzne walki .
U progu XX wieku rosło zadłużenie i uzależnienie kraju od obcego , głównie amerykańskiego kapitału .
Z powodu niewypłacalności i pod pretekstem konfliktów wewnętrznych w 1916 rozpoczęła się trwająca do 1924 amerykańska interwencja wojskowa .
Po okresie rządów prezydenta Horacio Vásqueza ( 1924-30 ) , do władzy doszedł Rafael Leónidas Trujillo .
Rządził on w dyktatorski sposób , utrzymując fasadę demokracji .
Reżim opierał się na wojsku i jedynej legalnej partii politycznej , Partii Dominikańskiej .
Trujillo odwoływał się do idei `` hispanidad '' ( kultywowania tradycji hiszpańskich ) i „ wybielenia ” społeczeństwa dominikańskiego .
W 1937 armia przeprowadziła masakrę haitańskich imigrantów .
W roku 1938 Dominikana była jedynym państwem na świecie deklarującym gotowość przyjęcia na wniosek Roosvelta żydowskich uchodźców z Niemiec i Austrii .
Na okres rządów dyktatora przypadła korzystna koniunktura gospodarcza , która poskutkowała likwidacją zagranicznego zadłużenia ( niemniej powszechna była korupcja , a rodzina Trujillo kontrolowała 75 % majątku narodowego ) .
W 1960 prezydenturę objął Joaquín Balaguer , który złagodził reżim .
Do całkowitego upadku dyktatury doszło w 1961 po zabójstwie Trujillo .
Wybory w 1962 wygrał Juan Bosch z Dominikańskiej Partii Rewolucyjnej ( PRD ) .
Uchwalił on nową demokratyczną konstytucję , ale już w 1963 został obalony w wojskowym zamachu stanu .
Władzę do 1965 sprawowała junta .
W tym samym roku grupa młodych oficerów armii – zwolenników Boscha – podjęła zbrojną próbę przejęcia władzy ( tzw .
ruch konstytucjonalistyczny ) .
Podczas walk stronę rządową wsparły interwencyjne oddziały USA .
Po ich wycofaniu w 1966 do władzy powrócił Balaguer .
W 1978 wybory wygrał Antonio Guzmán Fernández z PRD .
W 1982 prezydentem został partyjny kolega Fernándeza , Salvador Jorge Blanco .
Rządy Blanco zdominowały trudności ekonomiczne , wdrażanie dyskusyjnego programu liberalnych reform i konflikty wewnątrz rządzącej partii .
W 1986 na urząd powrócił Balaguer , który wygrał wybory jako kandydat Chrześcijańsko-Społecznej Partii Reformistycznej .
Zyskał on reelekcję w 1990 i 1994 .
W 1996 władzę objął Leonel Fernández z Partii Wyzwolenia Dominikany ( PLD ) .
Dzięki aktywnej polityce międzynarodowej Fernández zdobył uznanie za granicą , jednak jego popularność w kraju topniała .
W 2000 roku w wyborach prezydenckich zwyciężył Hipólito Mejía z PRD .
W 2004 na stanowisko powrócił Leonel Fernández i rządził do 2012 , kiedy zastąpił go Danilo Medina .
W 2020 prezydentem został Luis Abinader .
Demografia .
73 % Dominikańczyków to ludzie mieszanego pochodzenia rasowego , biali stanowią 16 % mieszkańców a czarni 11 % .
Około 1/4 ludności Republiki Dominikańskiej zamieszkuje stolicę kraju , Santo Domingo .
Drugim pod względem ludności miastem jest Santiago de los Caballeros .
Podział administracyjny .
Dominikana podzielona jest na 31 prowincji oraz obszar wydzielony stolicy ( `` Distrito Nacional '' ) .
Język .
W Republice Dominikańskiej językiem urzędowym jest hiszpański .
Język ten wprawdzie odbiega nieco od tego ( lub tych – jako że w Hiszpanii występuje również wiele różnych dialektów ) , który używany jest na Półwyspie Iberyjskim , jednak nie na tyle , aby stwarzało to problemy z komunikowaniem się .
Różnice te zaznaczają się zarówno na podłożu leksyki , jak i wymowy .
