Indyk domowy
Indyk domowy ( `` Meleagris gallopavo gallopavo var .
domesticus '' ) – ptak hodowlany z rodziny kurowatych , hodowany na całym świecie .
Stanowi formę udomowioną indyka .
Historia .
Indyki zostały udomowione przez Azteków w czasach prekolumbijskich .
Do Europy sprowadził je w 1497 roku Giovanni Caboto .
Istotne dla rozwoju hodowli stały się ptaki przywiezione w XVI wieku .
Do Polski trafiły w następnym wieku .
Hodowla .
Ptaki hodowane są zarówno na skalę masową w halach , jak i w gospodarstwach rolnych .
Pozyskuje się z nich głównie mięso .
Indyki zalicza się do trzech typów użytkowych :
Kultura .
Indyk domowy jest przyrządzany w Ameryce jako tradycyjna potrawa na Dzień Dziękczynienia .
Rasy .
Istnieje wiele ras indyka domowego .
Występuje upierzenie czarne , brązowe , białe oraz białe z czarną obwódką .
W Polsce najpopularniejsze są rasy Bronz , Bronz szerokopierśny oraz Beltsville .
