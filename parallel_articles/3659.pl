Donieck ( stacja kolejowa )
Donieck – stacja kolejowa w Doniecku , w obwodzie donieckim , na Ukrainie .
Znajduje się na niej 5 peronów .
Podczas II wojny światowej został zniszczony dworzec kolejowy .
W 1951 roku został zbudowany nowy dworzec stacji Stalino , zaprojektowany przez architekta I. Woroncowa .
Budynek z 1951 r. jest monumentalny , o tradycyjnym osiowo centralnym systemie przestrzennej organizacji .
Przy stacji Donieck działa Muzeum Historii i Rozwoju Kolei , które zostało powołane w dniu 4 sierpnia 2000 roku , w związku ze 130 rocznicą powstania Kolei Donieckiej .
W związku z Mistrzostwami Europy w Piłce Nożnej w 2012 r. zmodernizowano i odrestaurowano dworzec kolejowy z 1951 r. i zbudowano dwa nowe dworce kolejowo-przesiadkowe na transport drogowy .
Kompleks dworcowy stacji Donieck otwarto uroczyście 22 maja 2012 r .
Przy dworcu z 1951 r. stoi nowo zbudowana , dla kolejarzy Cerkiew św .
Mikołaja w Doniecku .
