Obwód doniecki
Obwód doniecki ( ) – jeden z 24 obwodów Ukrainy .
Leży we wschodniej części Ukrainy , przy granicy z Rosją .
Oficjalną stolicą obwodu jest Donieck .
Z powodu trwającej okupacji miasta przez prorosyjskich separatystów tymczasową siedzibą administracji obwodowej jest od 13 października 2014 roku Kramatorsk ( wcześniej , od 13 czerwca do 11 października 2014 , był nią Mariupol ) .
Obwód graniczy na zachodzie z obwodem zaporoskim i dniepropetrowskim , na północnym zachodzie z charkowskim , na wschodzie z ługańskim i na południowym wschodzie z obwodem rostowskim Federacji Rosyjskiej .
W południowo-wschodniej części obwodu działała samozwańcza Doniecka Republika Ludowa , do czasu nielegalnej aneksji obwodu przez Rosję 30 września 2022 roku .
11 maja 2014 roku w obwodzie odbyło się referendum niepodległościowe nieuznawane przez władze ukraińskie .
Obwód leży na styku trzech historycznych regionów : Zaporoża , Ukrainy Słobodzkiej i Wojska Dońskiego .
