Dźagdalpur
Dźagdalpur ( hindi जगदलपुर , trl .
`` Jagdālpur '' ) – miasto w środkowo-wschodnich Indiach , w południowej części stanu Ćhattisgarh , w dystrykcie Bastar , około 240 km w linii prostej na południe od stolicy stanu – Rajpuru .
Jest siedzibą administracyjną dystryktu .
W 2011 było ósmym pod względem liczby ludności miastem w stanie , najludniejszym w dystrykcie .
Zamieszkiwało je 125 463 osoby , co stanowiło ok. 8,9 % ludności dystryktu .
