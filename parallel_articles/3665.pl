Działdowo
Działdowo ( , prus . )
– miasto w Polsce , w województwie warmińsko-mazurskim , w powiecie działdowskim .
Położone jest na Wzniesieniach Mławskich , nad rzeką Działdówką ( nazwa odcinka Wkry ) ; w pobliżu przebiega droga ekspresowa .
Działdowo leży na Mazurach , w południowej części historycznych Prus Górnych , na obszarze dawnej ziemi Sasinów .
Jest największym miastem powiatu działdowskiego , 11. w województwie i 213. w Polsce .
Jedyne miasto na Mazurach , które leżało w granicach II Rzeczypospolitej .
Według danych z 1 stycznia 2018 Działdowo liczyło 21 370 mieszkańców .
