Nazwa .
Kronikarz Jan Długosz określił miejscowość jako Dzrezgon ( 25 IX 1429 ) oraz wymienił określenie funkcji komtura dzierzgońskiego jako Dzirzgouiensis ( 26 V 1429 ) , Dzirgouensis ( 14 VI 1430 ) i dalej jako Dzyrgon , Dzirgon , Dzyrgon .
W tekście pokoju toruńskiego pojawiła się nazwa Dzierzgon .
Demografia .
Według danych z 2 maja 2013 r. miasto miało 5564 mieszkańców .
