Dziesław ( województwo dolnośląskie )
Dziesław ( niem .
`` Deichslau '' ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie lubińskim , w gminie Ścinawa .
W latach 1975–1998 miejscowość administracyjnie należała do województwa legnickiego .
Historia .
Pierwsza wzmianka o Dziesławiu pochodzi z 1218 roku i zawiera łacińska nazwę osady `` Villa Tyslinii .
`` W późniejszych dokumentach wieś występuje jako `` Villa Thesselini '' ( 1235 ) , `` Dyslov '' ( 1310 ) , `` Dysselov '' ( 1331 ) , `` Deuchslawiense `` ( 1687 ) , `` Deischlau '' ( od 1736 do 1945 r. ) W ciągu stuleci zmieniali się właściciele miejscowości .
Wśród nich znaleźli się przedstawiciele takich rodów , jak : von Tschammer , von Braun , von Kottwitz , von , Kretschamer czy Herman von Deisslau od którego prawdopodobnie pochodzi współczesna nazwa osady .
Najstarszym znanym właścicielem miejscowych dóbr był zmarły w 1588 roku Georg von Braun .
W 1653 roku Dziesław był w posiadaniu Georga von Langenau .
W rękach rodu von Langenau wieś pozostawała do początku XIX wieku , kiedy to majątek wraz z miejscowością nabyła rodzina von Schkopp .
Następnie od 1827 roku krótko przebywała tu rodzina von Teichmann , od której w roku 1856 cały majątek nabył radca urzędowy Emil Metscher .
