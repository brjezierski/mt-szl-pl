Historia .
Wyspy zostały odkryte w 1493 przez Kolumba .
Osiedlili się tam koloniści holenderscy oraz angielscy piraci .
Skład etniczny zmienił się na skutek sprowadzania na wyspy afrykańskich niewolników .
W 1672 nastąpił podział Wysp Dziewiczych na brytyjskie i duńskie .
Jako pierwsza przez Duńską Kompanię Zachodnioindyjską została skolonizowana wyspa Saint Thomas , później Saint John .
Następnie przejęła ona Saint Croix , będącą przedtem posiadłością francuską .
W 1754 Duńskie Wyspy Dziewicze zostały kolonią koronną .
W 1917 zostały wykupione za 25 mln dolarów przez Stany Zjednoczone , z przeznaczeniem na bazę strategiczną ( w 1916 roku na tę transakcję zgodzili się Duńczycy w referendum ) .
Od roku 1954 posiadają wewnętrzną autonomię , a od 1970 same wybierają gubernatora .
Geografia .
Archipelag wysp należy do śródziemnomorza amerykańskiego i leży w tak zwanym antylskim łuku wulkanicznym .
