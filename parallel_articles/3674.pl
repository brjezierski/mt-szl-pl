Wyspy Dziewicze Stanów Zjednoczonych
Wyspy Dziewicze Stanów Zjednoczonych ( `` United States Virgin Islands '' ) – terytorium zależne Stanów Zjednoczonych w Ameryce Środkowej , w basenie Morza Karaibskiego , obejmujące 3 duże wyspy wulkaniczne i kilkadziesiąt mniejszych wysepek koralowych w południowej części archipelagu Wysp Dziewiczych .
Największe wyspy to : Saint Croix – 214,7 km² , Saint Thomas – 80,9 km² i Saint John – 50,8 km² .
Powierzchnia terytorium wynosi 346,4 km² , zamieszkuje go osób według spisu powszechnego z ( 2000 ) .
