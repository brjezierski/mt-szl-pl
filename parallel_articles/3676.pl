Dzikowiec ( wąwóz )
Dzikowiec – wąwóz w Ojcowskim Parku Narodowym ( OPN ) .
Jest bocznym , orograficznie prawym odgałęzieniem wąwozu Jamki .
Ma wylot w odległości około 0,5 km od wylotu wąwozu Jamki .
Wznosi się w kierunku południowym , w końcowym odcinku ulega spłyceniu i kończy się w lesie poniżej pól uprawnych Czajowic .
Wylot wąwozu Dzikowiec do Jamek jest skalisty .
Pomiędzy skałami znajduje się Jaskinia Krakowska i Jaskinia Biała .
W skupisku skał naprzeciwko wylotu Dzikowca znajduje się jaskinia Pustelnia .
Cały obszar wąwozu Dzikowiec to obszar ochrony ścisłej .
Przejście wąwozem dozwolone tylko za zgodą dyrekcji OPN .
