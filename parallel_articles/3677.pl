Dziwnów
Dziwnów ( hist .
`` Dyuennow '' , `` Diuenow '' , `` Dievenau '' , niem .
`` Dievenow '' ) – miasto w północno-zachodniej Polsce , nad Morzem Bałtyckim , w woj .
zachodniopomorskim , w powiecie kamieńskim , siedziba gminy miejsko-wiejskiej Dziwnów przy drodze nr 102 .
Miejscowość wypoczynkowa z letnim kąpieliskiem morskim i portem morskim .
Według danych z 31 grudnia 2013 r. miasto miało 2736 mieszkańców .
Położenie .
Dziwnów leży w środkowej części wybrzeża woj .
zachodniopomorskiego , w północnej części powiatu kamieńskiego .
Miasto położone jest nad Morzem Bałtyckim , przy ujściu cieśniny Dziwny do morza .
Od południa miasto leży nad Zalewem Kamieńskim i Zatoką Wrzosowską , które są częścią Dziwny .
Miejscowość znajduje się na Pobrzeżu Szczecińskim , część zachodnia na Półwyspie Międzywodzkim na wyspie Wolin , natomiast wschodnia na mierzei należącej do mezoregionu Wybrzeże Trzebiatowskie .
Według danych z 1 stycznia 2014 powierzchnia miasta wynosi 4,97 km² .
Dziwnów znajduje się w północnej części Pomorza Zachodniego .
