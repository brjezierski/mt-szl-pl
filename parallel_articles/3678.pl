Dziwnówek
Dziwnówek ( niem .
`` Klein Dievenow '' ) – wieś w północno-zachodniej Polsce , położona w województwie zachodniopomorskim , w powiecie kamieńskim , w gminie Dziwnów , 4,5 km na wschód od mostu zwodzonego w Dziwnowie .
Według danych z 2008 r. wieś miała 404 mieszkańców .
W Dziwnówku nad morzem wyznaczono letnie kąpielisko obejmujące 400 m linii brzegowej .
W latach 1975–1998 miejscowość położona była w województwie szczecińskim .
