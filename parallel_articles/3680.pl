Dźwierzuty
Dźwierzuty ( dawniej `` Swersutten '' , niem .
`` Mensguth '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie szczycieńskim , w gminie Dźwierzuty .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Dźwierzuty .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
Miejscowość jest siedzibą gminy Dźwierzuty .
Znajduje się tu przystanek PKS , urząd gminy , ośrodek zdrowia , poczta , bank spółdzielczy z ogólnodostępnym bankomatem , gminny ośrodek kultury , sklepy .
Założona na planie owalnicy ze stawem pośrodku .
Na wysokiej skarpie znajduje się późnogotycki kościół ( później ewangelicki ) otoczony drzewami oraz cmentarzem .
