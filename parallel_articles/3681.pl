Dziekaństwo
Dziekaństwo ( dodatkowa nazwa w j. niem .
Dziekanstwo ) – wieś w Polsce położona w województwie opolskim , w powiecie opolskim , w gminie Komprachcice .
Nazwa .
Nazwa wsi ma polskie pochodzenie i wskazuje , że była ona własnością dziekana katedry opolskiej .
Wcześniejsze znane formy to Dikkanowitz lub Dziekanowice ( analogicznie dzierżawczą formę mają nazwy okolicznych miejscowości : Komprachcice , Chmielowice , Żerkowice , Szczepanowice , Boguszyce ) .
Spis geograficzno-topograficzny miejscowości leżących w Prusach z 1835 roku , którego autorem jest J.E .
Muller , notuje obecnie używaną , polską nazwę miejscowości `` Dziekaństwo '' .
Historia .
Pierwszy zapis o wsi pochodzi z 1531 – działał w niej młyn , należący do wspomnianego dziekana .
W 1811 roku , w efekcie przeprowadzonej w Królestwie Prus sekularyzacji dóbr kościelnych , dwunastu kmieci odkupiło od państwa grunty i podzieliło je między siebie .
W 1845 w Dziekaństwie mieszkało 139 , a w 1980 - 179 osób .
Młyn wodny funkcjonował we wsi jeszcze w XIX wieku , nie jest jednak znana data zakończenia jego działalności .
Podczas plebiscytu w 1921 mieszkańcy Dziekaństwa wykazali najmniej `` proniemiecką '' postawę ze wszystkich miejscowości dzisiejszej gminy Komprachcice – za Niemcami padły 52 głosy , za Polską - 51 .
Według spisu powszechnego z lat 20 .
XX wieku we wsi mieszkało już 212 osób – wszyscy byli katolikami .
W latach 1936-1945 miejscowość nosiła nazwę `` Dechantsdorf '' .
Po II wojnie światowej , kiedy wieś znalazła się w granicach Polski , nie zmienił się jej charakter – nadal pozostaje najmniejszą miejscowością gminy Komprachcice – zamieszkują ją 223 osoby .
