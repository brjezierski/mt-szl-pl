Dzięczyna
Dzięczyna ( niem .
`` Dzientschin '' ) – wieś w Polsce położona w województwie wielkopolskim , w powiecie gostyńskim , w gminie Poniec .
Wieś jest siedzibą sołectwa `` Dzięczyna '' w którego skład wchodzi również miejscowość Dzięczynka .
Historia .
Wieś Dzienczyna położona była w 1580 roku w powiecie kościańskim województwa poznańskiego .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako `` Dzięczyn '' należała do wsi większych w ówczesnym pruskim powiecie Kröben ( krobskim ) w rejencji poznańskiej .
Dzięczyn należał do okręgu bojanowskiego tego powiatu i stanowił odrębny majątek , którego właścicielem był wówczas ( 1846 ) H. Unruh .
Według spisu urzędowego z 1837 roku wieś liczyła 337 mieszkańców , którzy zamieszkiwali 53 dymy ( domostwa ) .
Wieś rycerska , własność księcia Zygmunta Czartoryskiego , położona była w 1909 roku w powiecie gostyńskim rejencji poznańskiej w Wielkim Księstwie Poznańskim .
W latach 1975–1998 miejscowość administracyjnie należała do województwa leszczyńskiego .
