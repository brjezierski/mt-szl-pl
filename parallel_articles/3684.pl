Według urzędowego spisu opracowanego przez Komisję Nazw Miejscowości i Obiektów Fizjograficznych ( KNMiOF ) nazwa tego jeziora to `` Dzierżążno '' .
Nazwa pobliskiej wsi zapisana jako `` Derisno '' pochodzi z 1241 roku .
Jej podstawą jest nazwa rośliny wodnej dzierzęga lub też dzierżęga .
Wyraz ten bywa odnoszony do rzęsy lub trawy turzycy , ale na Kaszubach jest to knieć błotna .
Rośnie ona w 2010 r. przy brzegu tego jeziora .
