Dżahilijja
Dżahilijja ( ) – w islamie , termin określający okres w historii ludów arabskich przed pojawieniem się islamu .
Dosłownie dżahilijja oznacza „ czas nieświadomości , ciemnoty ” .
Wiadomości o kulturze i religii Arabów z tego okresu zawarte są w źródłach asyryjskich , greckich i rzymskich , a także w Biblii i Koranie ( sura 3 aja 154 ; sura 33 aja 33 czy sura 48 aja 26 ) .
Arabowie prawdopodobnie nie stworzyli harmonijnego systemu mitologicznego w okresie przed Mahometem .
Najważniejszym źródłem traktującym o bóstwach staroarabskich jest `` Żywot wysłannika Boga '' ( `` Sirat Rasul Allah '' ) , autorstwa Ibn Hiszama .
Wymienieni są tam bogowie Al-Wadd , Al-Kajs , Al-Uzza , Al-Lat i inni .
Charakterystyczny dla Arabów był kult wielkich głazów .
Już na wiele wieków przed islamem Arabowie pielgrzymowali do świątyni Al-Kaaba , by czcić tamtejszy Czarny Kamień .
Koran ( 71:23 ) wymienia pięć bóstw z tego okresu : Wadd , Suwa , Jaghuth , Ja ’ uq oraz Nasr .
