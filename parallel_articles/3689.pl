Dżakarta
Dżakarta ( ) – stolica Indonezji , w północno-zachodniej części Jawy , druga aglomeracja świata z 31 760 000 mieszkańców .
Nazewnictwo .
Wcześniejsze nazwy miasta to : Sunda Kelapa ( 397–1527 ) , Jayakarta ( 1527–1619 ) , Batavia ( 1619–1942 ) oraz Djakarta ( 1942–1972 ) .
Geografia .
Dżakarta jest położona na północno-zachodnim wybrzeżu Jawy , przy ujściu rzeki Ciliwung do zatoki Dżakarta , która jest częścią Morza Jawajskiego .
Północna część Dżakarty jest równinna i leży na wysokości ok. 8 m n.p.m .
Jest to przyczyną częstego zalewania miasta wodami morskimi .
Południowa część jest pagórkowata .
Przez miasto przepływa ok. 13 rzek , głównie płynących z południa w stronę Morza Jawajskiego .
Najważniejszą rzeką jest Ciliwung , która dzieli miasto na część wschodnią i zachodnią .
Klimat .
Zgodnie z klasyfikacją klimatyczną Köppena , Dżakarta charakteryzuje się gorącym i wilgotnym równikowym klimatem sawann ( `` Aw '' ) .
Środek pory wilgotnej wypada w styczniu ( z miesięcznymi opadami równymi 350 milimetrów ) , natomiast suchej przypada w sierpniu ( średnie miesięczne opady równe 60 milimetrów ) .
Przez cały rok w mieście występuje wysoka wilgotność powietrza z dzienną temperaturą wahającą się pomiędzy 24 i 34 °C .
Historia .
Miasto zostało założone pod koniec IV w. jako Sunda Kelapa .
W 1527 r. znalazło się pod kontrolą sułtanatu Bantam ( wówczas zmieniono jego nazwę na `` Jayakarta '' ) .
Pod koniec XVI w. na Jawie pojawili się Holendrzy .
W 1619 r. Holendrzy pod dowództwem Jana Pieterszoona Coena zdobyli miasto i założyli na jego terenie twierdzę `` Batavia '' , która stała się główną siedzibą holenderskiej Kompanii Wschodnioindyjskiej i kolonii holenderskiej Indii Wschodnich , nazywane też Djakarta .
W 1740 r. miała tu miejsce masakra chińskiej ludności , nazwana `` Batavia 1740 '' .
Silne trzęsienie ziemi zniszczyło miasto w 1699 r. Po kilku latach w rękach brytyjskich w 1814 r. Batavia powróciła w ręce Holendrów .
W okresie 1942–1945 miasto znajdowało się pod okupacją wojsk japońskich .
Po II wojnie światowej , mimo ogłoszenia niepodległości , miasto zostało zajęte przez Holandię .
Dopiero w 1949 r. miasto znalazło się w niepodległej Indonezji i stało się jej stolicą .
Administracja .
Oficjalnie Dżakarta nie jest miastem , ale prowincją ze specjalnym statusem stolicy Indonezji .
