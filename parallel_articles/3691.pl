Dżibuti
Dżibuti ( fr .
`` Djibouti '' , arab .
جيبوتي ) , Republika Dżibuti ( fr .
`` République de Djibouti '' , arab .
جمهورية جيبوتي ) – państwo we wschodniej Afryce , nad Zatoką Adeńską i cieśniną Bab al-Mandab .
Graniczy z Somalią ( a dokładniej z Somalilandem ) , Etiopią i Erytreą .
Dawne nazwy Dżibuti : Somali Francuskie , Francuskie Terytorium Afarów i Isów .
Polityka .
Ustrój polityczny .
Według konstytucji z 1992 roku , Dżibuti jest republiką , w której głową państwa jest prezydent , wybierany w wyborach powszechnych na 6-letnią kadencję .
Pierwszym prezydentem był Hasan Guled Aptidon .
Władze wykonawczą sprawują premier i rząd , powoływani przez prezydenta .
Władzę ustawodawczą sprawuje jednoizbowy parlament – Izba Deputowanych z 65 deputowanymi , wybieranymi w wyborach powszechnych na 5-letnią kadencję .
