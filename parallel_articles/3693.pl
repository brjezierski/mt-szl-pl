Dżudda
Dżudda , Dżedda ( , `` Ǧudda '' ) – miasto w zachodniej części Arabii Saudyjskiej , w prowincji Mekka , nad Morzem Czerwonym .
Główny ośrodek przemysłowy , handlowy i kulturalny kraju .
W mieście działa rafineria ropy naftowej , walcownia żelaza , huta aluminium , stocznia , a także fabryki artykułów chemicznych , wełnianych , skórzanych , mebli .
Stanowi ono także główny port handlowy kraju nad Morzem Czerwonym ( naftowy , kontenerowy ) , ważny port pasażerski ( główny punkt tranzytowy pielgrzymek do Mekki ) oraz rybacki .
W 2017 r. liczyła 4 296 000 mieszkańców .
