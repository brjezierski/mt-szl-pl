Dżurdżeni
Dżurdżeni ( ) – lud pochodzenia tunguskiego , zamieszkujący Mandżurię od co najmniej XI wieku .
Od XVII wieku nazywa się go Mandżurami .
Dżurdzeni na terenie północnych Chin utworzyli państwo , tzw .
dynastię Jin ( 1115–1234 ) .
Nazwa `` Dżurdżeni '' ( `` Nüzhen '' ) pojawia się na początku X wieku .
Jakkolwiek najwcześniejsze wzmianki o tym narodzie pojawiają się jeszcze w czasach przedchrześcijańskich , kiedy w chińskich księgach wymieniano go jako `` Sushen '' albo `` Jusen '' .
Nazwa ta wywodzi się być może od koreańskiej , `` Joseon '' .
Na przełomie XVI i XVII wieku Dżurdżenów zjednoczył Nurhaczy , który zreformował system władzy , tworząc Osiem Chorągwi .
