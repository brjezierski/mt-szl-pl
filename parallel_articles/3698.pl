Eartha Kitt
Eartha Kitt ( ur .
17 stycznia 1927 w North , zm .
25 grudnia 2008 w Weston ) – amerykańska aktorka , piosenkarka , artystka kabaretowa .
Życiorys .
Kitt urodziła się jako Eartha Mae Keith 17 stycznia 1927 , na plantacji bawełny w niewielkim mieście North w Karolinie Południowej .
Jej matka , Annie Mae Keith , była pochodzenia afrykańskiego i czirokeskiego .
Choć nie zostało to oficjalnie potwierdzone , powszechnie uważa się , że ojciec Earthy Kitt był pochodzenia niemieckiego , a sama artystka została poczęta w wyniku gwałtu .
Ponadto , miał być on synem właścicieli plantacji , na której urodziła się piosenkarka .
Kitt była wychowywana przez matkę , a następnie jej rodzinę .
W latach 40. wyjechała do Nowego Jorku , gdzie ukończyła Metropolitan Vocational High School ( obecnie High School of Performing Arts ) .
Kitt rozpoczęła swoją karierę w 1943 , w zespole Katherine Dunham Company , w którym występowała do 1948 .
