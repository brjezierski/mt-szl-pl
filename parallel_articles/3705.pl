Easter egg
Easter egg ( dosł .
z ang .
„ jajko wielkanocne ” ) – ukryte przed użytkownikiem treści w interaktywnych produktach cyfrowych ( gry , aplikacje , filmy ) lub jawne nawiązanie do innej treści , ale w oczywisty sposób niepasujące do treści/fabuły produktu ( często łamanie czwartej ściany ) .
Mają najczęściej akcent humorystyczny lub nawiązują do innego znanego produktu ( gry/filmu/postaci/książki itd . ) .
Znalezienie takiego nawiązania często wymaga wykonania odpowiednich czynności , wciśnięcia kombinacji klawiszy , dostania się w trudno dostępny zakamarek , zdobycia wszystkich ukrytych przedmiotów itp. , chociaż coraz częściej wymagają jedynie skojarzenia ze strony użytkownika ( np .
tekst/dialog mówiony , będący cytatem z filmu/książki , strój postaci czy plakat reklamowy ) .
Geneza .
Nazwa nawiązuje do zabawy w „ polowanie na ukryte jajka wielkanocne ” , w której dzieci muszą zebrać do koszyka wszystkie ukryte przez dorosłych wielkanocne jajka ( najczęściej na świeżym powietrzu , ale zabawa może też odbywać się w domu ) .
W trakcie produkcji filmu „ The Rocky Horror Picture Show ” twórcy postanowili zabawić się w te grę .
Część jaj nie została odnaleziona i można dostrzec je w poszczególnych momentach filmu .
Gry komputerowe .
Pierwszy easter egg w historii gier komputerowych wywodzi się z tytułu `` Adventure '' .
Z tą grą związane jest także powstanie terminu „ easter egg ” .
W branży elektronicznej rozrywki easter eggi mają często charakter humorystyczny , choć pełnią również inne funkcje :
Obecnie określenie używane jest również w kryptografii .
Jeden z typów dynamicznego cyfrowego znaku wodnego używanego w oprogramowaniu .
Aktualnie coraz mniej stosowany ze względu na łatwość wytropienia w kodzie oprogramowania przy pomocy narzędzi debugujących .
