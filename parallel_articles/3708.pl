Eberswalde ( krater marsjański )
Eberswalde – krater uderzeniowy na powierzchni Marsa o średnicy około 62 km .
Nazwa .
Decyzją Międzynarodowej Unii Astronomicznej w 2006 roku został nazwany od miasta Eberswalde w Niemczech .
Geologia krateru .
Krater Eberswalde znajduje się na obszarze Margaritifer Terra na południowych terenach wyżynnych na Marsie .
Powstał ponad 3,7 miliarda lat temu , w okresie noachijskim historii Marsa .
Został on częściowo pogrzebany przez materię wyrzuconą przez późniejsze uderzenie , które utworzyło sąsiedni krater Holden o ponaddwukrotnie większej średnicy .
Dobrze zachowana jest tylko północno-wschodnia część obrzeża krateru .
