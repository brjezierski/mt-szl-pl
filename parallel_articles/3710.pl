Ecser
Ecser – węgierska wioska i gmina położona niedaleko Budapesztu , w pobliżu lotniska Budapest Liszt Ferenc .
Posiada również słowacką nazwę ( `` Ečer '' ) .
Jej powierzchnia wynosi 13,1 km² a ludność ponad 3,6 tys .
( styczeń 2011 ) , przy gęstości zaludnienia 278,55 os./1 km² .
Historia .
Pierwsza wzmianka o miejscowości pochodzi z 15 grudnia roku 1315 , ale wioska istnieje prawdopodobnie od roku 896 , gdy na te tereny przybyli Węgrzy .
Według legendy jej nazwa została nadana przez węgierskiego wodza Arpada .
W trakcie tureckiego panowania ( 1526-1686 ) wymarła , głównie po oblężeniu pobliskiej Budy .
Ludzie osiedlili się w niej z powrotem w roku 1699 .
Mieszkają w niej Węgrzy i słowacka mniejszość .
Zabytki .
We wsi znajduje się katolicki kościół z roku 1740 .
