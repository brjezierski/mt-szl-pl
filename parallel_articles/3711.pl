Ed Sheeran
Ed Sheeran , właśc .
Edward Christopher Sheeran ( ur .
17 lutego 1991 w Halifaksie ) – brytyjski piosenkarz , autor tekstów , gitarzysta , producent muzyczny i aktor , wykonujący muzykę z pogranicza popu , rocka , folku i hip-hopu .
Życiorys .
Wczesne lata .
Urodził się w Halifaksie , handlowym mieście w Calderdale w West Yorkshire , jako drugi syn kustosza artystycznego i wykładowcy Johna oraz projektantki biżuterii Imogen .
Jego starszy brat Matthew jest kompozytorem muzyki klasycznej .
Ich dziadkowie ze strony ojca są Irlandczykami – dziadek William był protestantem , a babcia Anne – katoliczką .
Jako dziecko wraz z rodziną przeprowadził się do Framlingham w Suffolk .
Często podróżował z rodzicami po Londynie , gdzie pracowali we własnej firmie Sheeran Lock , zajmującej się doradztwem artystycznym .
Jako dziecko słuchał muzyki artystów , takich jak m.in .
Bob Dylan i Eric Clapton , zaś pierwszą płytą studyjną , jakiej przesłuchał , była album `` Irish Heartbeat '' Van Morrisona .
W wieku czterech lat Sheeran zaczął śpiewać w lokalnym chórze kościelnym .
W młodym wieku nauczył się grać na gitarze .
Mając jedenaście lat , razem z ojcem poszedł na koncert Damiena Rice ’ a w Irlandii , który zainspirował go do dalszego tworzenia muzyki .
Swoje pierwsze teksty piosenek zaczął pisać w trakcie nauki w Liceum im .
