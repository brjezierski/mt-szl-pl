Ed'mans
Ed'mans − śląski zespół folkowy założony przez Manfreda Potykę i Edwarda Sikorę .
Nazwa pochodzi od imion jego założycieli : `` Ed '' od Edwarda , `` man '' od Manfreda '' a `` s '' od `` Silesia '' łacińskiej nazwy Śląska .
Ed'mans wykonuje muzykę typową dla kultury górnośląskiej , większość tekstów utworów śpiewana jest po śląsku .
Kilkanaście razy występował na koncertach w Polsce i Niemczech , stale gości na antenie TVS i Radia Piekary .
Ma w dorobku cztery płyty .
W 2009 roku z okazji dziesięciolecia istnienia duetu ukazał się dwupłytowy album `` Dwa na jeden '' .
