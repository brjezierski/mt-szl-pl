Eddsworld
Eddsworld – brytyjska niezależna seria internetowych animacji , komiksów i gier przeglądarkowych , stworzona w programie Adobe Flash przez Edda Goulda dla serwisu Newgrounds .
Seria była rozpowszechnia również na DeviantArt , YouTube , Albino Blacksheep , Tumblr oraz BBC .
Fabuła .
Seria opowiada o przygodach trójki mężczyzn – Edda , Toma i Matta , którzy mają takie same imiona jak prawdziwi twórcy , a także wzorowali na nich swoje osobowości .
Seria charakteryzuje się wykorzystaniem kalamburów .
