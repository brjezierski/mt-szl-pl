Miasto słynie z licznych dorocznych imprez i festiwali , z których najsłynniejszymi są Edinburgh International Festival , Fringe ( festiwale teatralne ) oraz Military Tattoo ( występy orkiestr wojskowych ) , odbywające się w sierpniu .
