Edmond de Goncourt
Edmond Louis Antoine Huot de Goncourt ( ur .
26 maja 1822 w Nancy , zm .
16 lipca 1896 w Champrosay ) – francuski pisarz , krytyk literacki i wydawca .
Utworzył Académie Goncourt .
Ufundował nagrodę Prix Goncourt przyznawaną przez tę akademię za `` najwybitniejsze francuskojęzyczne dzieło literackie '' .
Nagrodę przyznaje się co roku w grudniu , od 1903 roku .
Do laureatów należą m.in .
Marcel Proust , Simone de Beauvoir , Michel Tournier , Marguerite Duras i Romain Gary ( ten ostatni dwa razy ) .
