Edmonton
Edmonton – miasto położone w zachodniej Kanadzie , leżące na preriach kanadyjskich , nad rzeką Saskatchewan Północny .
Edmonton stało się stolicą kanadyjskiej prowincji Alberta 1 września 1905 r .
Ludność miasta wynosi 730 372 ( 2006 ) , a w aglomeracji 1 034 945 ( 2006 ) mieszkańców .
Jest to drugie co do wielkości miasto Alberty po Calgary .
Jest jednym z najnowocześniejszych miast zachodniej Kanady .
Wraz z Calgary tworzy serce gospodarcze i finansowe bogatej prowincji Alberty .
Łącznie miasta te skupiają przeszło 80 % populacji całej prowincji , co daje obraz stopnia urbanizacji tych terenów .
Ważny ośrodek naukowo-kulturalny ( Uniwersytet Alberty , liczne festiwale ) .
