Edmund Hillary
Edmund Percival Hillary ( ur .
20 lipca 1919 w Auckland , zm .
11 stycznia 2008 tamże ) – nowozelandzki himalaista i polarnik , z zawodu pszczelarz .
Życiorys .
W latach 1951–1952 uczestnik trzech wypraw himalajskich .
29 maja 1953 zdobył wraz z Szerpą Tenzingiem Norgayem , jako pierwsi w historii , szczyt Mount Everest ( 8848 m )
Do historii przeszły jego słowa wypowiedziane po zejściu ze szczytu Everestu : „ W końcu załatwiliśmy skurczybyka ” ( `` We finally knocked the bastard off '' ) .
Nie przypuszczał nawet , że za pośrednictwem BBC usłyszał je cały świat .
Za wyczyn został uhonorowany przez królową angielską Elżbietę II tytułem szlacheckim , natomiast Nowa Zelandia umieściła podobiznę Hillary'ego na pięciodolarowym banknocie , czyniąc go pierwszą osobą , która znalazła się na banknocie za życia .
W latach 1954–1967 kierował wyprawami nowozelandzkimi w Himalaje i na Antarktydę .
W trakcie ekspedycji zorganizowanej w ramach Międzynarodowego Roku Geofizycznego ( 1955–1958 ) założył bazę Scotta na Wyspie Rossa i przygotowując trasę wyprawy transantarktycznej Viviana Fuchsa dotarł 4 stycznia 1958 do bieguna południowego .
Następnie dołączył do nowozelandzkiej służby dyplomatycznej , którą pełnił do 1989 .
W 1960 założył fundusz powierniczy – Himalayan Trust , który prowadzi akcje pomocy dla Szerpów .
Dzięki działalności funduszu w himalajskich wioskach wybudowano dla Szerpów 27 szkół , 2 szpitale , 12 ośrodków zdrowia , mosty , wodociągi , a nawet małe lotniska .
Został honorowym obywatelem Nepalu .
Tam też w katastrofie lotniczej zginęły jego pierwsza żona Louise i córka Belinda .
Podczas wizyty w Polsce 17 czerwca 2004 został odznaczony przez Prezydenta RP Aleksandra Kwaśniewskiego Krzyżem Komandorskim Orderu Zasługi Rzeczypospolitej Polskiej w uznaniu wybitnych zasług dla światowego himalaizmu oraz za działalność charytatywną .
