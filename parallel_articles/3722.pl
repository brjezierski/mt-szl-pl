Edmund Niziurski
Edmund Niziurski ( ur .
10 lipca 1925 w Kielcach , zm .
9 października 2013 w Warszawie ) – polski prozaik , publicysta , scenarzysta i dramaturg , a także socjolog i prawnik .
Twórca popularnych książek dla dorosłych i młodzieży , cechujących się charakterystycznym , nieco absurdalnym humorem słownym .
Najbardziej znane to `` Księga urwisów '' ( 1954 ) , `` Sposób na Alcybiadesa '' ( 1964 – szkolna lektura , w 1978 wpisana na Listę Honorową IBBY ) , `` Niewiarygodne przygody Marka Piegusa '' ( nagroda `` Orle Pióro '' przyznana przez czytelników „ Płomyka ” w 1970 roku ) , `` Klub włóczykijów '' , `` Naprzód , Wspaniali !
`` , `` Adelo , zrozum mnie !
`` , `` Awantura w Niekłaju '' , `` Siódme wtajemniczenie '' .
Życiorys .
Urodził się 10 lipca 1925 r. w Kielcach , w rodzinie urzędniczej .
Syn Stanisława Niziurskiego , urzędnika państwowego , i Leokadii z Grethów .
Był najstarszym spośród trojga rodzeństwa ( brat Mirosław został kompozytorem , profesorem sztuk muzycznych ; siostra Zofia ) .
Uczył się w Gimnazjum im .
J. Śniadeckiego w Kielcach , jednak naukę przerwała wojna .
Wraz z rodziną został ewakuowany na Węgry , gdzie kontynuował naukę w gimnazjum polskim dla uchodźców .
