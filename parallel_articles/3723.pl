Edmundston
Edmundston – miasto w Kanadzie , w prowincji Nowy Brunszwik i hrabstwie Madawaska .
Aż do 1850 miasto nosiło nazwę `` Petit-Sault '' , dzisiejsza nazwa została nadana na cześć Edmunda Walkera Heada , gubernatora porucznika Nowego Brunszwiku w latach 1848–1854 .
Liczba mieszkańców Edmundston wynosi 16 643 .
Język francuski jest językiem ojczystym dla 93,4 % , angielski dla 4,6 % mieszkańców ( 2006 ) .
