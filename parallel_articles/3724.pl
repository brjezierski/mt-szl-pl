Edremit
Edremit – miasto w Turcji w prowincji Balıkesir , niedaleko greckiej wyspy Lesbos .
Według danych na rok 2009 miasto zamieszkiwało 50 464 osób .
W starożytności miasto nazywało się Adramyttion ( gr .
`` Άδραμύττιον '' , łac .
`` Adramyttium '' ) i zostało wspomniane w Nowym Testamencie ( Dz 27,2 ) .
Miasto zbudował Adramys , brata ostatniego króla Lidii – Krezusa .
