Eduard Szewardnadze
Eduard Szewardnadze ( , ; ur .
25 stycznia 1928 w Gurii , zm .
7 lipca 2014 w Tbilisi ) – radziecki i gruziński polityk , minister spraw zagranicznych ZSRR od 2 lipca 1985 do 20 grudnia 1990 oraz od 19 listopada do 25 grudnia 1991 , prezydent Gruzji od 26 listopada 1995 do 23 listopada 2003 .
Kariera w czasach ZSRR .
Szewardnadze w wieku 18 lat został instruktorem Komsomołu i dwa lata później wstąpił do partii komunistycznej .
W 1959 został członkiem Rady Najwyższej Gruzińskiej Socjalistycznej Republiki Radzieckiej , w 1965 został ministrem porządku publicznego Gruzińskiej SRR , a w 1968 spraw wewnętrznych i generałem milicji .
Zasłynął wtedy walką z korupcją , doprowadzając do skazania wielu urzędników .
W 1972 zastąpił na stanowisku I Sekretarza KC Komunistycznej Partii Gruzji skompromitowanego w aferach korupcyjnych Wasyla Mżawanadzego .
Na tym stanowisku kontynuował walkę ze skorumpowanymi urzędnikami , lecz także z dysydentami .
W 1976 został członkiem Komitetu Centralnego , a w 1978 Biura Politycznego KC KPZR .
W 1985 został ministrem spraw zagranicznych ZSRR , dołączając do grona względnie młodych reformatorów skupionego wokół Gorbaczowa .
Odegrał bardzo ważną rolę w upadku ZSRR , odmawiając pomocy komunistycznym reżimom w Europie Środkowej i umożliwiając tym samym demokratyczne przemiany .
W 1987 r. wyraził w imieniu ZSRR wstępną zgodę na zjednoczenie państw niemieckich , RFN i NRD .
Pod koniec lat 80. jego droga rozeszła się z Gorbaczowem , który dążył do zachowania ZSRR .
W 1990 – na parę miesięcy przed nieudanym puczem w Moskwie – przewidując nadchodzący wstrząs polityczny , podał się do dymisji .
Swoją rezygnację określił jako protest przeciwko „ rodzącej się dyktaturze ” .
W listopadzie 1991 powrócił na krótki czas na stanowisko , po czym podał się do dymisji razem z Gorbaczowem , po formalnym rozwiązaniu Związku Radzieckiego .
Prezydentura Gruzji .
Pierwszym prezydentem nowo niepodległej Gruzji został Zwiad Gamsachurdia ( syn słynnego pisarza gruzińskiego , Konstantyna Gamsachurdii ) , dysydent więziony przez Szewardnadzego w latach 70 .
Został on jednak obalony w wyniku puczu w styczniu 1992 i musiał zbiec do Czeczenii .
