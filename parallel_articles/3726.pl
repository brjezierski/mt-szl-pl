Edyta Górniak
Edyta Anna Górniak ( ur .
14 listopada 1972 w Ziębicach ) – polska piosenkarka , kompozytorka , autorka tekstów , producentka i osobowość telewizyjna , na początku kariery także aktorka musicalowa .
Członkini Akademii Fonograficznej ZPAV oraz Stowarzyszenia Artystów Wykonawców Utworów Muzycznych i Słowno-Muzycznych SAWP .
Honorowa obywatelka gminy Ziębice oraz Honorowy Ambasador Stolicy Polskiej Piosenki Opole .
Debiutowała w 1986 jako wokalistka założonego przez siebie zespołu System B. W 1990 otrzymała wyróżnienie w koncercie „ Debiuty ” podczas 27 .
Krajowego Festiwalu Polskiej Piosenki w Opolu .
Następnie rozpoczęła karierę jako aktorka musicalowa , występując w musicalu `` Metro '' wystawianym na scenach Teatru Dramatycznego w Warszawie i Studia Buffo oraz na Broadwayu .
W 1994 , reprezentując Polskę z utworem „ To nie ja ! ” , zajęła drugie miejsce w finale 39 .
