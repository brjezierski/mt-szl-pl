Eeklo ( 1953 )
Eeklo ( M911 ) – belgijski trałowiec z lat 50 .
XX wieku , jedna z 26 zbudowanych dla Belgijskiej Marynarki Wojennej jednostek amerykańskiego typu `` Adjutant '' .
Stępkę okrętu położono w sierpniu 1951 roku w stoczni Hodgdon Brothers w East Boothbay , a w skład marynarki wojennej Belgii został przyjęty w czerwcu 1953 roku .
W 1969 roku jednostkę zwrócono USA , po czym została przekazana Tajwanowi i wcielona do marynarki wojennej Republiki Chińskiej pod nazwą „ Yung Ching ” .
Okręt wycofano ze służby w maju 1995 roku .
Projekt i budowa .
Projekt trałowców typu `` Adjutant '' ( `` Blackbird '' ) powstał jako rozwinięcie drugowojennych jednostek typu `` YMS '' .
Okręty miały w większości zasilić floty państw sojuszniczych , w ramach Major Defense Acquisition Program ( MDAP ) .
