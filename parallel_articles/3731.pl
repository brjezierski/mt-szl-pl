Bluszcz ( film )
Bluszcz – polski film obyczajowy z 1982 roku w reżyserii Hanki Włodarczyk .
Fabuła .
Trzydziestoletnia Kinga właśnie rozwiodła się z mężem i samotnie wychowuje córkę .
Nowy romans nie układa się po jej myśli , coraz bardziej nudzi ją też praca .
Sfrustrowana kobieta bierze urlop , aby spełnić swe marzenie i napisać książkę .
