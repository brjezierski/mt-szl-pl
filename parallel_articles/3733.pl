Egilsstaðir
Egilsstaðir – miasto we wschodniej Islandii , w gminie Múlaþing ( do 2020 roku w gminie Fljótsdalshérað ) , w regionie Austurland .
Stanowi główną miejscowość we wschodniej części wyspy - na początku 2021 zamieszkiwało je 2552 osób .
Miejscowość położona jest na wschodnim brzegu jeziora Lagarfljót .
Na jego drugim brzegu położona jest miejscowość Fellabær .
Zostało założone pod koniec XIX w. Prawa miejskie otrzymało w 1947 roku .
