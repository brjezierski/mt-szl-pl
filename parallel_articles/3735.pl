Egmond aan Zee
Egmond aan Zee – wieś w gminie Bergen , w prowincji Holandia Północna , w Holandii .
Według danych na rok 2018 wieś zamieszkiwało 4615 osób , a gęstość zaludnienia wyniosła 4502 os./km2 .
Klimat .
Klimat jest umiarkowany .
Średnia temperatura wynosi 8 °C .
Najcieplejszym miesiącem jest lipiec ( 18 °C ) , a najzimniejszym miesiącem jest luty ( 0 °C ) .
