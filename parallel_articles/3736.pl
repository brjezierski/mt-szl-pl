Eichwalde ( przystanek kolejowy )
Eichwalde ( ) – przystanek osobowy w Eichwalde , w kraju związkowym Brandenburgia , w Niemczech , na południe od Berlina .
Znajduje się na linii Berlin – Görlitz i obsługuje pociągi linii S46 i S8 S-Bahn w Berlinie .
Według DB Station & amp ; Service ma kategorię 5 .
Położenie .
Przystanek znajduje się około 750 metrów na zachód od centrum Eichwalde .
Granica Berlina jest oddalona o około 500 metrów .
Sąsiednie ulice to `` Heinrich-Heine-Allee '' i `` August-Bebel-Allee '' .
Najbliższa stacja na południu Zeuthen znajduje się około 3 km , a na północy Berlin-Grünau , która jest oddalona o około pięć kilometrów .
Stacja znajduje się w obszarze taryfowym C Verkehrsverbundes Berlin-Brandenburg .
