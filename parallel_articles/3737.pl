Eidsvoll ( stacja kolejowa )
Eidsvoll – stacja kolejowa w Eidsvoll , w gminie Eidsvoll w regionie Akershus w Norwegii , jest oddalona od Oslo o 67,5 km .
Położenie .
Jest końcową stacją linii Dovrebanen , ponadto leży na linii Hovedbanen .
Leży na wysokości 127 m n.p.m .
Nowy budynek stacji został oddany do użytku w roku 1998 .
