Język ajnu
Język ajnu , także ajni , ejnu lub abdal ( nazwa własna : äynú ( ئەينۇ ) [ ɛjˈnu ] ) — język wschodnioturecki , używany w ujgurskim regionie autonomicznym Chińskiej Republiki Ludowej , uważany czasem za dialekt języka ujgurskiego .
W słownictwie zaznacza się wyraźny wpływ języków irańskich , podczas gdy fonetyka i gramatyka zachowują turkijski charakter .
Jego użytkowników stanowi grupa etniczna , licząca mniej niż 30 tys .
