Eksabajt
Eksabajt ( skrót EB ) , eksbibajt ( skrót EiB ) – jednostki używane w informatyce oznaczające odpowiednio trylion ( 1.000.000.000.000.000.000 = 1018 ) bajtów i 260 , czyli 1.152.921.504.606.846.976 = 10246 bajtów .
Stosowane m.in .
do określania pojemności zespołów pamięci masowych .
Zgodnie ze standardem IEC 60027-2 w systemie przedrostków dwójkowych ( binarnych ) obowiązują zależności :
1 EiB = 1024 PiB = 1024 · 1024 TiB = 1024 · 1024 · 1024 GiB = 1024 · 1024 · 1024 · 1024 MiB = 10245 kiB = 10246 B
natomiast w systemie przedrostków dziesiętnych SI :
1 EB = 1000 PB = 1000 · 1000 TB = 1000 · 1000 · 1000 GB = 1000 · 1000 · 1000 · 1000 MB = 10005 kB = 10006 B
Potocznie jednak eksabajt w informatyce utożsamiany jest z eksbibajtem ( skrót EiB ) .
Maksymalna pojemnością pamięci operacyjnej , którą może teoretycznie zaadresować procesor 64-bitowy , jest 16 EiB ( 264 bajtów ) .
