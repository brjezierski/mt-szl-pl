Położenie .
Państwo położone w północno-zachodniej Ameryce Południowej nad Oceanem Spokojnym .
Graniczy z Kolumbią od północy i z Peru od południa .
Do Ekwadoru należą również Wyspy Galapagos położone na Oceanie Spokojnym ok. 1000 km na zachód od części kontynentalnej .
Warunki naturalne .
Obszar Ekwadoru rozciąga się od nizin na wybrzeżach Oceanu Spokojnego w zachodniej części kraju , poprzez łańcuchy górskie Andów , po zachodnie krańce Niziny Amazonki na wschodzie .
Linia brzegowa jest słabo rozwinięta , naprzeciw jedynej dużej zatoki Guayaquil leży wyspa Puná o powierzchni ok. 900 km² .
