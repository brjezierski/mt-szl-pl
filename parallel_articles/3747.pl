Krzywa Wieża w Pizie
Krzywa Wieża w Pizie ( ) – jedna z najbardziej znanych budowli świata , odwiedzana rocznie przez około 10 milionów turystów ; symbol miasta Pizy .
W istocie jest dzwonnicą ( kampanilą ) katedralną i należy do kompleksu zabudowań w stylu romańskim na Campo dei Miracoli , wpisanych w 1987 roku na listę światowego dziedzictwa UNESCO .
Historia .
Wieża jest zbudowana z białego marmuru , liczy osiem kondygnacji i 54,98 m wysokości .
Jej masę ocenia się na 14 tysięcy ton .
Została zaprojektowana przez Guglielmo i Bonanno Pisano .
Budowę rozpoczęto w 1174 r. i prowadzono ją w 3 etapach przedzielonych długimi przerwami .
Cała budowa trwała 199 lat .
W drugim etapie , prowadzonym w latach 1272–1278 , wieża zaczęła się przechylać .
Mimo niebezpieczeństwa , jeszcze w 1350 r. dodano ostatnie piętro , w którym umieszczono dzwony .
Podczas budowy dokonywano korekt mających zniwelować przechył .
Dzięki odchyleniu wieży od pionu Galileusz mógł wykorzystać ją w roku 1600 do zademonstrowania niezależności czasu spadania ciał od ich masy ( zob .
eksperyment Galileusza ze spadkiem swobodnym ciał ) .
Należy jednak zaznaczyć , że eksperyment z wykorzystywaniem krzywej wieży przez uczonego uważany jest przez wielu naukowców za mit z powodu braku wiarygodnych źródeł .
W XIX wieku zaczęto podejmować pierwsze próby powstrzymania przechylania się wieży , co przyniosło jednak przeciwny skutek .
W 1890 odchylenie osiągnęło 5,24° .
Pierwszy pomiar odchylenia z użyciem precyzyjnej aparatury został wykonany w 1911 r. Ustalono wówczas , że wynosi ono 5,25° , co odpowiadało wychyleniu wieży o 4,22 m pomiędzy najwyższym a najniższym piętrem .
Przechył cały czas wzrastał , z rosnącą prędkością – w latach 30. rósł on średnio o 4″ rocznie , a pod koniec lat 80. o 6″ rocznie .
Od 1991 r. przechylenie jest monitorowane automatycznie co 4 h z dokładnością do 1/100 mm .
W 1993 r. po północnej stronie wieży ustawiono obciążenie ze sztab ołowianych o masie 600 t , co po raz pierwszy doprowadziło do zmniejszenia przechyłu .
W 1995 r. zaczął on jednak znowu wzrastać i obciążenie zwiększono do 870 t .
W kwietniu 2011 r. po 20 latach zakończono prace konserwatorskie w Krzywej Wieży .
Usunięto z jej murów grubą warstwę zanieczyszczeń i ślady niszczycielskiej działalności zwiedzających .
Specjaliści zebrali wszystkie kawałki marmuru , które odpadły i umieścili je z powrotem we właściwych miejscach murów wieży .
Wieżę odrestaurował zespół pod kierunkiem Michelego Jamiolkowskiego , profesora geotechniki z Politechniki w Turynie .
Zastosowana metoda polegała na wzmocnieniu fundamentów i niedopuszczeniu do dalszego przechyłu .
Zespół Jamiołkowskiego dał Krzywej Wieży gwarancję na 300 lat .
Z obliczeń wynika , że po przekroczeniu przechyłu 5,44° wieża może runąć , jednak nie nastąpiło to nawet , gdy przechył wyniósł 5,5° .
W 2013 r. przechył wynosił 3,99° , jednak pod koniec drugiej dekady XXI w. wynosił on ok. 5,5° , natomiast w 2023 r. podano , że wynosi on 5,115° .
