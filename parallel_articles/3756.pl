El Prat de Llobregat
El Prat de Llobregat ( wym .
, pot .
`` El Prat '' ) – miejscowość w Hiszpanii w Katalonii położona w comarce Baix Llobregat , leży w obszarze metropolitarnym Barcelony w delcie rzeki Llobregat .
Ponad ¼ powierzchni miasta zajmuje międzynarodowy port lotniczy Barcelona-El Prat .
