Elbasan
Elbasan ( dawniej Scampa , Albanopolis ) – miasto w środkowej Albanii nad rzeką Shkumbin .
Ośrodek administracyjny okręgu Elbasan w obwodzie Elbasan .
W październiku 2008 roku zamieszkiwane było przez 126 081 mieszkańców .
W mieście rozwinął się przemysł materiałów budowlanych , drzewny , spożywczy oraz metalurgiczny .
