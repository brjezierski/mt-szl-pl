Elbląg ( rzeka )
Elbląg ( niem .
`` Elbing '' ) – rzeka na Żuławach Wiślanych o długości 14,5 km .
Wypływa z jeziora Druzno , uchodzi do Zalewu Wiślanego .
Rzeka jest żeglowna na całej długości ( od Elbląga traktowana jako morskie wody śródlądowe ) .
Poprzez kanały posiada połączenie :
Rzeka Elbląg stanowi oś żeglugowego systemu transportowego .
Poprzez Kanał Jagielloński i Nogat posiada połączenie z Wisłą , a poprzez Kanał Elbląski łączy się z Iławą i Ostródą .
Na rzece znajduje się port morski w mieście Elbląg .
W okresach suszy woda z rzeki za pomocą systemu melioracyjnego może być wykorzystywana do nawadniania obszarów rolniczych na Żuławach Elbląskich .
W zależności od wahań poziomu wody w Zalewie Wiślanym spowodowanych `` wpychaniem '' wód Bałtyku do Cieśniny Piławskiej przez silne wiatry północne i północno-wschodnie , kierunek spływu wód rzeki Elbląg odwraca się i powoduje spiętrzenie wody jeziora Druzno .
Znaczna część dorzecza rzeki obejmuje tereny depresyjne , w tym największą depresję w Polsce 1,8 m p.p.m. , położoną na południowy wschód od miasta Elbląg pomiędzy wsią Raczki Elbląskie a rzeką Tyną .
Prawie na całej długości rzeki Elbląg i jej dopływach w ich dolnym biegu występują obwałowania przeciwpowodziowe .
Według niektórych źródeł do przepływu rzeki zalicza się również przepływ rzeki Dzierzgoń wypływającej w powiecie sztumskim , o długości 45 kilometrów i uchodzącej do jeziora Druzno .
W północno-wschodniej części zlewni występuje Park Krajobrazowy Wysoczyzny Elbląskiej .
