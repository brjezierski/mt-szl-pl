Elchesheim-Illingen
Elchesheim-Illingen – gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Mittlerer Oberrhein , w powiecie Rastatt , wchodzi w skład związku gmin Durmersheim .
Leży ok. 7 km na północ od Rastatt , ok. 2 km na zachód znajduje się granica z Francją na Renie .
