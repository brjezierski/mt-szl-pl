Elihu Thomson
Elihu Thomson ( ur .
29 marca 1853 w Manchesterze , zm .
13 marca 1937 w Swampscott w stanie Massachusetts ) – amerykański inżynier , wynalazca i przemysłowiec .
Profesor i rektor ( w latach 1920–1922 ) Massachusetts Institute of Technology .
Założyciel ( w roku 1883 ) przedsiębiorstwa produkującego urządzenia elektryczne Thomson-Houston Electric Company , które w 1892 po połączeniu z Edison Electric Light Company stworzyło spółkę General Electric .
Wynalazki Thomsona .
Thomson wynalazł między innymi prądnicę trójfazową prądu zmiennego , prądnicę i transformator wielkiej częstotliwości , licznik energii elektrycznej .
W roku 1882 razem z Edwinem J. Houstonem wynalazł praktyczny system oświetlenia łukowego .
Opracował też metodę zgrzewania oporowego .
W roku 1887 zademonstrował odkryte przez siebie zjawisko skaczących pierścieni ( zwanych też „ pierścieniami Thomsona ” ) , wykorzystywane w edukacji jako efektowna demonstracja prawa Lenza i prawa Faradaya .
W 1909 został wyróżniony Medalem Edisona za „ wybitne zasługi w naukach , inżynierii i sztukach elektrycznych , w których jego wkład widoczny jest przez ostatnie 30 lat ” .
