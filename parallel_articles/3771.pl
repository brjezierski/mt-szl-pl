Elizówka ( powiat lubelski )
Elizówka – wieś w Polsce położona w województwie lubelskim , w powiecie lubelskim , w gminie Niemce .
Położona około 5 km na północ od Lublina przy drodze krajowej nr i węźle drogowym „ Lublin-Rudnik “ stanowiącym element obwodnicy północnej Lublina .
W tej miejscowości rozpoczyna się również droga wojewódzka nr .
Od 2000 r. na terenie miejscowości funkcjonuje Rynek Elizówka zarządzany przez spółkę Lubelski Rynek Hurtowy S.A. , najdalej wysunięty na wschód Rynek w Unii Europejskiej .
Zajmuje powierzchnię 18 hektarów .
Ma tu też siedzibę Lubelski Oddział Regionalny Agencji Restrukturyzacji i Modernizacji Rolnictwa .
W latach 1975–1998 miejscowość należała administracyjnie do województwa lubelskiego .
Wieś stanowi sołectwo gminy Niemce .
Według Narodowego Spisu Powszechnego z roku 2011 wieś liczyła 859 mieszkańców .
