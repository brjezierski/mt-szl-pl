Ellensburg
Ellensburg – miasto w północno-zachodniej części Stanów Zjednoczonych leżące w centrum stanu Waszyngton .
Stolica hrabstwa Kittitas .
Populacja miasta wynosi 15 414 mieszkańców ( stan na rok 2000 ) .
Powierzchnia wynosi 17,2 km² .
W mieście Ellensburg ma swoją siedzibę Central Washington University ( CWU ) .
