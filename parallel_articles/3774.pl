Ellwangen ( Jagst )
Ellwangen ( Jagst ) – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Ostwürttemberg , w powiecie Ostalb , siedziba wspólnoty administracyjnej Ellwangen ( Jagst ) .
na przedpolu Jury Szwabskiej , ok. 12 km na północ od Aalen , przy drodze krajowej B290 , autostradzie A7 i linii kolejowej ( Ulm–Aschaffenburg ) .
