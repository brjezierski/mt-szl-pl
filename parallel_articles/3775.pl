Elmen
Elmen – gmina położona na zachodzie Austrii , w kraju związkowym Tyrol , w powiecie Reutte .
Nazwa gminy pochodzi od wiązu , który został też przedstawiony w herbie .
Przez Elmen przebiega szlak rowerowy Bike Trail Tirol i liczne szlaki piesze w okoliczne góry ( m.in .
Stablalpe , Klimmspitze i Elmer Kreuzspitze ) .
Położenie .
Elmen leży w dolinie rzeki Lech , poniżej ujścia potoku Streimbach , pomiędzy Alpami Lechtalskimi od wschodu i Alpami Algawskimi od zachodu ( skraj Hornbachkette ) .
Część gminy objęta jest ochroną przyrody w ramach Parku Natury Tiroler Lech .
Dojazd zapewnia prowadząca doliną droga krajowa B198 , rozgałęziająca się w stronę miejscowości Bschlabs przez przełęcz Hahntennjoch w kierunku Imst ( dojazd poza sezonem zimowym ) .
Zabytki i atrakcje turystyczne .
W Elmen znajduje się zabytkowy barokowy kościół Trzech Króli `` ( Hl .
Drei Könige ) '' , a także kilka kapliczek i droga kalwaryjska .
