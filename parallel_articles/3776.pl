Elmhurst ( Illinois )
Elmhurst – jedno z przedmieść Chicago , położone w hrabstwie DuPage w stanie Illinois .
Ludność w 2006 roku wynosiła 43 298 .
Historia miasta .
Około 1836 roku pierwsi osadnicy z Europy dotarli w rejony gdzie obecnie położone jest Elmhurst .
W 1843 otwarta została tawerna , która szybko zyskała popularność wśród podróżujących z Chicago do Fox River Valley .
