Elsinoë veneta
Elsinoë veneta ( Burkh . )
Jenkins – gatunek grzybów z klasy `` Dothideomycetes '' .
U malin i jeżyn wywołuje chorobę o nazwie antraknoza maliny i jeżyny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Elsinoë , Elsinoaceae , Myriangiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1917 r. Walter Hagemeyer Burkholder nadając mu nazwę `` Plectodiscella veneta '' .
Obecną , uznaną przez Index Fungorum nazwę nadała mu Anna Eliza Jenkins w 1932 r .
Morfologia i rozwój .
