Elsinoë
Elsinoë Racib .
– rodzaj rodzaj grzybów z klasy `` Dothideomycetes '' .
Grzyby mikroskopijne , saprotrofy i pasożyty roślin .
Większość gatunków występuje na obszarach o klimacie tropikalnym .
2 gatunki występujące w Polsce powodują takie choroby roślin uprawnych , jak antraknoza róży i antraknoza maliny i jeżyny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Elsinoaceae , Myriangiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy : `` Bitancourtia '' Thirum .
