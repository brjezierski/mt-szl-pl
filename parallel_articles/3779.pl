Elverum ( stacja kolejowa )
Elverum – stacja kolejowa w Elverum , w regionie Hedmark w Norwegii , jest oddalony od Oslo Sentralstasjon o 158,38 km .
Leży na poziomie 187,8 m n.p.m .
Ruch pasażerski .
Leży na linii Rørosbanen , jednej z dwóch równoległych linii kolejowych prowadzących z Oslo do Trondheim .
Stacja obsługuje ruch dalekobieżny do Hamar , Røros oraz jedno połączenie dziennie do Trondheim S .
Obsługa pasażerów .
Poczekalnia , automat biletowy , parking , parking rowerowy , kiosk , skrytki bagażowe , przystanek autobusowy , postój taksówek .
Odprawa podróżnych odbywa się w pociągu .
