Elvis Presley
Elvis Aaron Presley ( wym .
[ ] ; ur .
8 stycznia 1935 w Tupelo , zm .
16 sierpnia 1977 w Memphis ) – amerykański piosenkarz i aktor .
Wykonywał muzykę z pogranicza rocka , popu , country , rock and rolla czy rockabilly .
Karierę muzyczną rozpoczął w 1954 , kiedy to podjął współpracę z Samem Phillipsem , właścicielem wytwórni muzycznej Sun Records .
Jako aktor zadebiutował rolą w filmie `` Kochaj mnie czule '' ( 1956 ) .
W 1958 został powołany do armii , a dwa lata później powrócił do nagrywania .
Przez większość lat 60 .
XX wieku grał w filmach i nagrywał do nich ścieżki dźwiękowe .
W 1968 wrócił do śpiewania na żywo po siedmioletniej przerwie .
W 1973 wziął udział w `` Aloha from Hawaii '' , pierwszym koncercie nadawanym przez satelitę .
Pozostaje jedną z ważniejszych ikon popkultury XX wieku .
Był jednym z popularniejszych muzyków popowych w USA , nawet w czasach tzw .
brytyjskiej inwazji , kiedy to rynek muzyczny zdominował nowoczesny , europejski rock and roll i rock .
Często nazywany „ Królem rock and rolla ” lub po prostu „ Królem ” .
Określony przez magazyn „ Billboard ” najlepszym artystą lat 50 .
Szacowany nakład wszystkich wydawnictw muzycznych Presleya na całym świecie sięgnął ponad 600 mln płyt .
W 1986 został upamiętniony w muzeum Rock and Roll Hall of Fame .
Z powodzeniem śpiewał muzykę country , rhythm and blues , pop , rock , a także gospel , za którą trzy razy otrzymał Grammy Awards ( za album `` How Great Thou Art '' z 1967 oraz `` He Touched Me '' z 1971 , a także za wykonanie na żywo utworu gospel `` How Great Thou Art '' 20 marca 1974 w Memphis ) .
