Elżbieta Mieszkówna
Elżbieta ( ur .
przed 1154 , zm .
2 kwietnia 1209 ) – księżniczka wielkopolska , księżna czeska , margrabina Łużyc Dolnych z dynastii Piastów .
Córka księcia wielkopolskiego Mieszka III Starego i prawdopodobnie jego pierwszej żony , Elżbiety , królewny węgierskiej .
Żona księcia Czech Sobiesława II z dynastii Przemyślidów , następnie margrabiego Łużyc Dolnych Konrada II z Wettynów .
Życiorys .
Dokładna data urodzenia Elżbiety nie jest znana .
Źródła nie podają , która żona Mieszka III Starego była jej matką .
Ze względu na imię w literaturze naukowej jest uważana za córkę pierwszej żony , Elżbiety węgierskiej .
Jako dziecko z pierwszego małżeństwa Mieszka III musiała urodzić się przed 1154 .
Część badaczy podaje , że Elżbieta przyszła na świat około 1152 .
Około 1173/1174 została poślubiona przez Sobiesława II , księcia Czech od 1173 .
Małżeństwo to było elementem szerokiej polityki dynastycznej posiadającego liczne potomstwo Mieszka III .
W efekcie zawartego porozumienia politycznego w 1176 polskie posiłki brały udział w wyprawie Sobiesława na posiadłości Babenbergów .
W 1178 książę czeski Fryderyk obległ Pragę , w której przebywała Elżbieta .
Po upadku stolicy księżna dostała się do niewoli , jednakże Fryderyk wspaniałomyślnie ją uwolnił .
27 stycznia 1179 Sobiesław poniósł klęskę w bitwie na przedpolach Pragi .
