Elżbietów ( Radomsko )
Elżbietów – część miasta Radomska położona w jego wschodniej , rolniczej , części , w rejonie ul .
Krańcowej .
Stanowi najdalej na wschód wysuniętą część Radomska .
Do 1977 odrębna miejscowość .
Historia .
Elżbietów to dawna wieś .
Do 1954 należał do gminy Dmenin w powiecie radomszczańskim ( 1867–1922 pn .
`` noworadomski '' ) , początkowo w guberni piotrkowskiej , a od 1919 w woj .
