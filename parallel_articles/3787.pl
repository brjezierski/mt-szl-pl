Eminem
Eminem , właśc .
Marshall Bruce Mathers III ( ur .
17 października 1972 w St. Joseph ) – amerykański raper , producent oraz aktor .
Członek amerykańskiej grupy hiphopowej D12 oraz część duetu hiphopowego z Detroit – Bad Meets Evil , który tworzy wraz z Royce da 5 ' 9 '' .
Piętnastokrotny zdobywca Nagrody Grammy oraz laureat Oscara .
W 2022 wprowadzony do Rock and Roll Hall of Fame .
Eminem jest jednym z najlepiej sprzedających się artystów na świecie oraz najlepiej sprzedającym się artystą pierwszej dekady XXI wieku .
Dwutygodnik „ Rolling Stone ” sklasyfikował go na 83. miejscu na liście 100 najlepszych artystów wszech czasów .
Ten sam magazyn nazwał Eminema „ Królem Hip Hopu ” .
Wliczając twórczość z D12 oraz Bad Meets Evil , Eminem osiągnął 10 albumów nr 1 na listach Billboard 200 .
Raper sprzedał ponad 240 milionów płyt na całym świecie .
Jest jednocześnie dzięki temu na szczycie listy najlepiej sprzedających się raperów .
W 1996 , jako undergroundowy raper , Eminem wydał swój debiutancki solowy album `` Infinite '' .
Album został źle przyjęty przez krytyków i bardzo źle się sprzedawał .
Marshall Mathers zyskał światową sławę w 1999 za sprawą swego debiutanckiego albumu dla dużej wytwórni – `` The Slim Shady LP '' , za ten album otrzymał również swoją pierwszą Nagrodę Grammy za Najlepszy album Rap .
Następne dwie płyty , `` The Marshall Mathers LP '' z 2000 oraz `` The Eminem Show '' z 2002 przyniosły mu dwie kolejne Grammy za Najlepsze albumy Rap , czyniąc go pierwszym artystą który otrzymał 3 Grammy za 3 kolejne albumy .
Eminem otrzymał jeszcze czwartą statuetkę Grammy za swój czwarty z rzędu album `` Encore '' wydany w 2004 .
Po trasie koncertowej w 2005 Eminem przerwał swoją karierę .
Powrócił po 4-letniej przerwie , wydając w maju 2009 album `` Relapse '' .
W 2010 wydał siódmy studyjny album zatytułowany `` Recovery '' .
