Emir Kusturica
Emir Kusturica , ( ur .
24 listopada 1954 w Sarajewie ) – jugosłowiański i serbski reżyser filmowy , scenarzysta , muzyk , także aktor .
Dwukrotnie nagrodzony Złotą Palmą na 38. i 48 .
MFF w Cannes ( 1985 , 1995 ) .
Życiorys .
Jeden z najbardziej utalentowanych i oryginalnych europejskich twórców filmowych dwóch ostatnich dekad XX wieku .
Reżyserię studiował na praskiej uczelni filmowej FAMU .
Z tego okresu pochodzi jego studenckie dzieło – `` Guernica '' .
Już jeden z pierwszych filmów , nakręconych po powrocie do ojczyzny , oparty na noweli Ivo Andricia `` Bar Titanic , '' zyskał uznanie krajowej krytyki .
Międzynarodową sensację wywołał nakręcony w 1985 `` Ojciec w podróży służbowej '' .
Nostalgiczna opowieść o dojrzewaniu w Jugosławii lat pięćdziesiątych XX w. zdobyła Złotą Palmę na 38 .
MFF w Cannes .
Film był także nominowany do Oscara dla najlepszego filmu nieanglojęzycznego .
Klasę reżysera potwierdził `` Czas Cyganów '' , film którego akcja rozgrywa się w środowisku bałkańskich Romów .
Był to pierwszy film Kusturicy , do którego muzykę skomponował Goran Bregović ; współpraca artystów urwie się dopiero po filmie `` Underground '' .
Kolejny film Kusturicy , `` Arizona Dream '' , jest jego pierwszym filmem anglojęzycznym .
W tej tragikomicznej baśni o poszukiwaniu szczęścia i spełnianiu marzeń w rolach głównych wystąpili Faye Dunaway i Johnny Depp .
W 1995 powstaje najgłośniejszy i wzbudzający największe kontrowersje film Kusturicy – `` Underground '' , niemal trzygodzinny fresk będący gorzkim rozliczeniem reżysera z Jugosławią i jej historią .
Film rozpoczyna się podczas niemieckiej okupacji , ostatnie jego sceny rozgrywają się w czasie wojny w Bośni .
`` Underground '' został uhonorowany Złotą Palmą na 48 .
MFF w Cannes .
Kusturica jest także muzykiem ; gra na gitarze w zespole `` No Smoking Orchestra '' .
Emir Kusturica pochodzi z rodziny muzułmańskiej , ale w roku 2005 przyjął chrzest w obrządku prawosławnym , przybierając imię Nemanja Kusturica .
Uroczystość odbyła się w Monastyrze Savina w miejscowości Herceg Novi w Czarnogórze .
Zasiadał w jury konkursu głównego na 46 .
MFF w Cannes ( 1993 ) .
Przewodniczył obradom jury na 56 .
MFF w Wenecji ( 1999 ) oraz na 58 .
MFF w Cannes ( 2005 ) .
Był również przewodniczącym jury sekcji „ Cinéfondation ” na 56 .
MFF w Cannes ( 2003 ) oraz sekcji „ Un Certain Regard ” na 64 .
MFF w Cannes ( 2011 ) .
Literatura .
`` Gdzie ja jestem w tej historii ?
`` .
Autobiografia Kusturicy , zatytułowana `` Gdzie ja jestem w tej historii ? , '' została wydana w Belgradzie w październiku 2010 roku przez wydawnictwo .
Początkowo wydana została w nakładzie 20 000 egzemplarzy wyłącznie na rynek Serbii , Czarnogóry i Bośni i Hercegowiny , w ciągu kolejnych kilku miesięcy doczekała się jeszcze trzech wydań .
W roku 2013 łączna liczba sprzedanych egzemplarzy przekroczyła 114 000 .
Książka została przetłumaczona i wydana w kilku krajach europejskich :
W Polsce książka wydana została w roku 2014 pod tytułem `` Gdzie ja jestem w tej historii ? ''
przez wydawnictwo Claroscuro .
`` Sto jada '' .
Druga książka Kusturicy , powieść `` Sto jada '' została wydana w Serbii w 2013 roku przez wydawnictwo Novosti AD .
W 2015 roku zostało wydane francuskie tłumaczenie książki , zatytułowane `` Étranger dans le mariage '' .
