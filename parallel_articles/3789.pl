Emmanuel Macron
( wym .
[ ] ; ur .
21 grudnia 1977 w Amiens ) – francuski polityk , urzędnik państwowy i bankowiec .
Minister gospodarki , przemysłu i cyfryzacji w latach 2014–2016 , od 2017 prezydent Republiki Francuskiej i z urzędu współksiążę Andory .
Życiorys .
Wykształcenie .
Kształcił się w jezuickim liceum w Amiens , następnie w paryskiej szkole średniej Lycée Henri-IV .
Uzyskał dyplom DEA z zakresu filozofii na Université de Paris X , został także absolwentem Instytutu Nauk Politycznych w Paryżu oraz École nationale d ’ administration .
