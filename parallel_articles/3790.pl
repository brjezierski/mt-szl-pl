Emmy Rossum
Emmy Rossum , właśc .
Emmanuelle Grey Rossum ( ur .
12 września 1986 w Nowym Jorku ) – amerykańska aktorka i piosenkarka .
Zagrała m.in .
w filmach `` Upiór w operze '' , `` '' czy `` Pojutrze '' .
Później grała w serialu `` Shameless – Niepokorni '' .
W 2007 roku ukazała się jej płyta zatytułowana `` Inside Out '' .
W 2007 roku Emmy wydała też płytę `` Caroll of the Bells '' z trzema świątecznymi piosenkami , a w 2013 album `` Sentimental Journey '' .
