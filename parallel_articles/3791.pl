Empoli ( stacja kolejowa )
Empoli ( włoski : `` Stazione di Empoli '' ) – stacja kolejowa w Empoli , w regionie Toskania , w prowincji Florencja , we Włoszech .
Znajduje się na linii Leopolda , a także na linii Centralna Toskania .
Według klasyfikacji RFI posiada kategorię `` złotą '' .
Dziennie obsługuje około 6 000 pasażerów .
