En Charod Me ’ uchad
En Charod Me ’ uchad ( hebr .
עין חרוד מאוחד ; ang .
En Harod Me'uhad ) – kibuc położony w Samorządzie Regionu Ha-Gilboa , w Dystrykcie Północnym , w Izraelu .
Członek Ruchu Kibuców ( `` Ha-Tenu ’ a ha-Kibbucit '' ) .
Położenie .
Kibuc En Charod Me ’ uchad jest położony na wysokości od 50 metrów p.p.m .
do 20 metrów n.p.m .
na północnej krawędzi Doliny Charod , na północy Izraela .
