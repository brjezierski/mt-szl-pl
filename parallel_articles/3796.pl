Andrzejewo ( powiat sokólski )
Andrzejewo – kolonia w Polsce położona w województwie podlaskim , w powiecie sokólskim , w gminie Sidra .
W latach 1975–1998 miejscowość należała administracyjnie do województwa białostockiego .
Przed 1556 r. wzniesiono tu dwór z murowanymi zabudowaniami i tarasowymi ogrodami otoczonymi z dwóch stron rzeką .
W 1 połowie XIX wieku w Andrzejewie zbudowano nowy drewniany dwór .
W 1863 roku właściciel Andrzejewa został wywieziony przez Rosjan na Syberię za udział w powstaniu styczniowym .
