Ennio Morricone
Ennio Morricone ( wym .
, ur .
10 listopada 1928 w Rzymie , zm .
6 lipca 2020 tamże ) – włoski kompozytor i dyrygent .
Był autorem muzyki do ponad 500 filmów .
Ponad 40 z napisanych przez niego ścieżek dźwiękowych zdobyło najważniejsze nagrody w kategoriach związanych z muzyką filmową .
W roku 2007 Amerykańska Akademia Filmowa przyznała mu honorowego Oscara za całokształt twórczości .
W 2016 roku otrzymał Oscara za muzykę do filmu `` Nienawistna ósemka '' ( 2015 ) .
Komponował od 1946 roku .
Do 2016 roku sprzedano ponad 70 milionów płyt z jego muzyką .
Życiorys .
Pierwszym nauczycielem muzyki włoskiego kompozytora był jego ojciec , który nauczył go czytać nuty i grać na kilku instrumentach .
Ennio Morricone był bardzo uzdolniony muzycznie .
Zaczął komponować mając 6 lat .
Dwa lata później , kiedy uczęszczał do Szkoły Podstawowej św .
Jana w Rzymie poznał swego przyszłego partnera artystycznego – Sergio Leone .
W wieku 12 lat wstąpił do Narodowego Konserwatorium im .
Świętej Cecylii , gdzie pobierał lekcje gry na trąbce u Umberto Semproniego .
W 1940 roku rozpoczął czteroletni kurs harmonii , który zakończył prawdopodobnie już po 6 miesiącach .
W pewnym momencie jeden z jego nauczycieli harmonii zasugerował mu , by zaczął studiować kompozycję .
Niedługo potem Morricone to uczynił .
Jego nauczycielem był Goffredo Petrassi .
Zanim Ennio Morricone ukończył konserwatorium ( w klasie trąbki , orkiestracji i kompozycji ) miał już na swoim koncie kilka ścieżek dźwiękowych do słuchowisk radiowych .
Był też czynnym trębaczem – grał w orkiestrze specjalizującej się w muzyce pisanej do filmów .
W 1946 Ennio Morricone otrzymał dyplom ukończenia studiów w klasie trąbki i w tym samym roku skomponował `` Il Mattino '' .
W 1952 otrzymał dyplom ukończenia studiów w klasie instrumentacji , a w 1954 – w klasie kompozycji .
Zasiadał w jury konkursu głównego na 37 .
MFF w Cannes ( 1984 ) oraz na 49 .
MFF w Wenecji ( 1992 ) .
30 sierpnia 2009 Ennio Morricone wystąpił w Polsce na Festiwalu Solidarności w Stoczni Gdańskiej .
29 września 2012 w hali „ Targów Kielce ” odebrał z rąk arcybiskupa Gianfranco Ravasiego Medal Per Artem ad Deum ( `` Przez sztukę do Boga '' ) , przyznawany przez Papieską Radę ds .
