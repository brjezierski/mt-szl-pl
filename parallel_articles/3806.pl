Entoloma allochroum
Entoloma allochroum Noordel .
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1982 r. Machiel Evert Noordeloos w ogrodzie w Holandii .
Morfologia .
Średnica 25–40 mm , początkowo stożkowaty z nieco podwiniętym brzegiem , w końcu wypukły z garbkiem i prostym brzegiem , słabo higrofaniczny ; w stanie wilgotnym lekko prześwitujący i prążkowany tylko na brzegu lub bez prążkowania .
Powierzchnia całkowicie aksamitna , przechodząca we włóknisto-łuskowatą z liliowym odcieniem , z szarobrązowymi łuskami na raczej bladym , różowo-szarym tle .
