Entoloma caccabus
Entoloma caccabus ( Kühner ) Noordel .
– gatunek grzyba z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1954 r. Robert Kühner nadając mu nazwę `` Rhodocollybus caccadus '' .
W 1982 r. Machiel Evert Noordeloos przeniósł go do rodzaju `` Entoloma '' .
Synonimy :
Morfologia .
