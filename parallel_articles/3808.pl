Entoloma cephalotrichum
Entoloma cephalotrichum ( P.D .
Orton ) Noordel .
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1960 r. Peter Darbishire Orton , nadając mu nazwę `` Leptonia cephalotrichum '' .
W 1979 r. Machiel Evert Noordeloos przeniósł go do rodzaju `` Entoloma '' .
