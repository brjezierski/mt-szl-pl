Entoloma cuneatum
Entoloma cuneatum ( Bres . )
M.M .
Moser – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1887 r. Giacomo Bresàdola , nadając mu nazwę `` Nolanea cuneata '' .
Obecną nazwę nadał mu Meinhard Moser w 1978 r .
Synonimy :
Morfologia .
Średnica 20–60 mm , początkowo ostro stożkowaty , stożkowaty lub dzwonkowaty , potem wypukły , w końcu płaskowypukły z wyraźnym garbkiem , higrofaniczny ; w stanie wilgotnym ciemnoczerwono-brązowy lub sepiowy z kontrastowo jaśniejszym , żółtym garbkiem , przezroczyście prążkowany na brzegu , w stanie suchym blednący do szaro- lub żółtawobrązowego , garbek długo pozostaje bledszy .
Powierzchnia naga , gładka , brzeg początkowo podwinięty , potem prosty .
