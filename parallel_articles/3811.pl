Entoloma exile ( Fr . )
Hesler – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1818 r. Elias Fries , nadając mu nazwę `` Agaricus exilis '' .
Obecną nazwę nadał mu Lexemuel Ray Hesler w 1967 r .
