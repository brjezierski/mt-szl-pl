Entoloma hispidulum
Entoloma hispidulum ( M. Lange ) Noordel .
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1946 r. Morten Lange , nadając mu nazwę `` Rhodophyllus hispidulus '' .
Obecną nazwę nadał mu Machiel Evert Noordeloos w 1982 r .
Synonimy :
Morfologia .
Średnica 5–25 mm , początkowo stożkowaty lub stożkowo-wypukły z podwiniętym brzegiem , później wypukły , zazwyczaj z wyraźnym garbkiem i odgiętym brzegiem , niehigrofaniczny , o nieprążkowanym i nieprześwitującym brzegu .
Powierzchnia od bladobrązowej do ciemnoszarobrązowej ( częściej ) , zwykle jaśniejsza w kierunku brzegów , silnie promieniście włóknisto-łuskowata , środek często z gęstymi , sterczącymi łuskami .
Od 14 do 26 , l = 1–3 , średnio gęste , wolne lub wąsko przyrośnięte , początkowo białe lub białoszare , potem różowe , na koniec brązowe lub szaraworóżowe .
