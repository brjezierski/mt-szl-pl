Entoloma hochstetteri
Entoloma hochstetteri ( Reichardt ) G. Stev .
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek został opisany jako `` Hygrophorus hochstetteri '' w 1866 r. przez Reichardta , nazwany został dopiero w 1962 r. przez Stevensona .
Nazwa gatunkowa upamiętnia niemieckiego przyrodnika Ferdinanda von Hochstettera .
Morfologia .
Wyróżnia się charakterystyczną niebieską barwą całych owocników , jedynie blaszki mają lekki czerwonawy odcień za sprawą zarodników .
Grzyb zawdzięcza barwę trzem barwnikom azulenowym .
Ma małe , delikatne owocniki .
Kapelusz osiąga do 4 cm średnicy i jest stożkowatego kształtu .
Jest barwy indygo z zielonkawym odcieniem i jedwabisty w dotyku .
Krawędź kapelusza jest ząbkowana i podwinięta do wewnątrz .
Blaszki cienkie , 3–5 mm szerokości , zasadniczo takiej samej barwy jak kapelusz , niekiedy z żółtawym odcieniem .
