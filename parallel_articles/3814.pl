Entoloma longistriatum
Entoloma longistriatum ( Peck ) Noordel .
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1911 r. Charles Horton Peck , nadając mu nazwę `` Leptonia longistriata '' .
Obecną nazwę nadał mu Machiel Evert Noordeloos w 1988 r .
