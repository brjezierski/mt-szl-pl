Morfologia .
Średnica 15–50 mm , początkowo stożkowaty , dzwonkowaty ze ściętym wierzchołkiem lub półkulisty z podwiniętym brzegiem , później wypłaszczający się i wyraźnie pępkowaty z odgiętym lub prostym brzegiem , higrofaniczny ; w stanie wilgotnym przezroczyście prążkowany do połowy promienia lub więcej , ciemno żółtobrązowy z ciemniejszym szarobrązowym środkiem , w stanie suchym blednący .
Powierzchnia drobno łuskowata lub pofałdowana na środku , promieniście włóknista w kierunku brzegu .
Od 12 do 40 , l = 1–7 , dość gęste , szeroko przyrośnięte lub zbiegające ząbkiem , brązowe do różowawo-brązowych .
Ostrza całe lub postrzępione tej samej barwy .
Wysokość 20–80 mm , grubość 2,5 mm , cylindryczny lub spłaszczony z podłużnym rowkiem , zwężający się lub rozszerzający w kierunku podstawy .
