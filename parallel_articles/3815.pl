Dzwonkówka sutkowata
Dzwonkówka sutkowata , rumieniaczek brodawkowaty ( `` Entoloma mammosum '' ( L. ) Hesler ) – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1763 r. Karol Linneusz nadając mu nazwę `` Agaricus mammosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Lexemuel Ray Hesler w 1967 r .
Synonimy :
Nazwę zwyczajową zaproponował Stanisław Chełchowski w 1898 r. , dwa lata wcześniej Franciszek Błoński używał nazwy rumieniaczek brodawkowaty .
Morfologia .
Owocniki drobne .
Kapelusz z wyraźnym garbem na środku , prążkowany , koloru łososiowego .
Blaszki w kolorze drewna , trzon wysmukły .
Występowanie i siedlisko .
Dzwonkówka sutkowata występuje w Europie i Ameryce Północnej .
W piśmiennictwie naukowym do 2003 r. podano na terenie Polski 5 stanowisk .
Wyrasta na ziemi i ściółce latem i jesienią na pastwiskach , łąkach , w zaroślach i w lasach .
Owocniki pojawiają się latem i jesienią , zwłaszcza we wrześniu i październiku .
