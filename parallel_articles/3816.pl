Entoloma nitens
Entoloma nitens ( Velen . )
Noordel .
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1921 r. Josef Velenovský , nadając mu nazwę `` Nolanea nitens '' .
Obecną nazwę nadał mu Machiel Evert Noordeloos w 1979 r .
