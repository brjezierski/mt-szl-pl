Entoloma pallens
Entoloma pallens ( Maire ) Arnolds – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1937 r. René Charles Maire , nadając mu nazwę `` Eccilia pallens '' .
Obecną nazwę nadał mu Eef Arnolds w 1982 r .
Synonimy :
Morfologia .
Średnica 15–33 mm , początkowo płasko-wypukły , później rozszerzający się z zagłębionym środkiem , niehigrofaniczny .
