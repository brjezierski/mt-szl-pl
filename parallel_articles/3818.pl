Dzwonkówka pasożytnicza
Dzwonkówka pasożytnicza ( `` Entoloma parasiticum '' ( Quél . )
Kreisel ) – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1879 r. Lucien Quélet , nadając mu nazwę `` Leptonia parasitica '' .
Obecną nazwę nadał mu Hanns Kreisel w 1984 r .
Synonimy :
Morfologia .
