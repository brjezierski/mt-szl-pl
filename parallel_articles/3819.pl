Dzwonkówka czerniejąca
Dzwonkówka czerniejąca ( `` Entoloma parkensis '' ( Fr . )
Noordel . )
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1851 r. Elias Fries nadając mu nazwę `` Agaricus parkensis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Machiel Evert Noordeloos w 1979 r .
Synonimy :
Nazwę zwyczajową zaproponował Władysław Wojewoda w 2003 r .
Występowanie i siedlisko .
Dzwonkówka czerniejąca występuje w kilku krajach Europy ( Hiszpania , Niemcy , Dania , Szwecja ) .
Jest bardzo rzadka .
Na terenie Polski w piśmiennictwie naukowym podano jedno stanowisko i to przed II wojną światową ( Kwidzyn , 1925 , 1933 r. ) .
Według W. Wojewody gatunek w Polsce wymarły .
Występuje w lasach liściastych pod jesionami i w murawach kserotermicznych na podłożu wapiennym , Owocniki wytwarza od sierpnia do października .
