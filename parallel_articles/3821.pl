Dzwonkówka gromadna
Dzwonkówka gromadna ( `` Entoloma sodale '' Kühner & amp ; Romagn .
ex Noordel . )
– gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 1,5–3 cm .
Początkowo dzwonkowaty , potem kolejno wypukły , płasko-wypukły i rozpostarty , zazwyczaj z niewielką wklęsłością , lub nawet lejkowaty .
Brzeg początkowo podgięty , potem wyprostowany .
Jest higrofaniczny .
W stanie wilgotnym przeźroczysty i prążkowany do połowy promienia , skórzasto-blado-brązowy z drobnymi , szarymi łuseczkami , szczególnie na środku .
Na obrzeżu zaś wełnisto-włóknisty , lun niemal łuseczkowaty , rzadko gładki .
