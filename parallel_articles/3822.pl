Dzwonkówka mała
Dzwonkówka mała ( `` Entoloma subradiatum '' ( Kühner & amp ; Romagn . )
M.M .
Moser ) – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entoloma , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1954 r. Robert Kühner i Henri Charles Louis Romagnesi nadając mu nazwę `` Rhodophyllus subradiatus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1978 r. Meinhard Michael Moser .
