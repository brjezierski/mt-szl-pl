Owadomorek muszy
Owadomorek muszy ( `` Entomophthora muscae '' ( Cohn ) Fresen . )
– gatunek grzybów należący do rzędu owadomorkowców ( `` Entomophthorales '' ) .
Jest to grzyb entomopatologiczny , który rozwija się w ciele owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Entomophthora , Entomophthoraceae , Entomophthorales , Incertae sedis , Entomophthoromycetes , Entomophthoromycotina , Entomophthoromycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1855 r. Ferdinand Julius Cohn nadając mu nazwę `` Empetra muscae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1856 r. Johann Baptist Fresenius .
Rozwój .
Owad może zostać zainfekowany zarodnikiem konidialnym wytwarzanym przez grzybnię `` Entomophthora muscae '' .
Zarodniki te są lepkie , po przyklejeniu się do ciała owada kiełkują .
Wydzielają przy tym enzymy rozpuszczające chitynowy oskórek owada .
