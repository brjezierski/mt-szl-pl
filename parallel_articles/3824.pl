Entyloma achilleae
Entyloma achilleae Magnus – gatunek podstawczaków należący do rodziny `` Entylomataceae '' .
Grzyb mikroskopijny , pasożyt krwawników ( `` Achillea '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Entyloma , Entylomataceae , Entylomatales , Exobasidiomycetidae , Exobasidiomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1900 r. Paul Wilhelm Magnus na krwawniku pospolitym w Niemczech .
Synonimy :
Charakterystyka .
Na porażonych liściach krwawnika powoduje powstawanie owalnych , prawie płaskich plam o długości 1–2 mm , początkowo białawo-żółtawych , ostatecznie ciemnobrązowaych .
Znajduje się w nich masa zarodników .
Zarodniki kuliste lub jajowate , 9 – 13 µm , gładkie złoto-żółte .
Listki krwawnika są drobne , spowodowane przez patogena zbrunatnienie listków jest nieznaczne , wskutek czego porażenie jest trudne do zauważenia .
Kiełkowanie zarodników oraz sposób zakażania żywiciela nie jest znany .
Znane jest występowanie `` Entyloma achilleae '' w niektórych krajach Europy .
W Polsce znany był już w 1938 r .. Podano kilka jego stanowisk .
Pasożyt jednodomowy , monofag .
Jego żywicielami są : krwawnik pospolity ( `` Achillea millefolium '' ) i krwawnik szlachetny ( `` Achillea nobilis '' ) .
