Entyloma bellidis
Entyloma bellidis Krieg .
– gatunek podstawczaków należący do rodziny `` Entylomataceae '' .
Grzyb mikroskopijny , pasożyt roślin z rodzajów stokrotka ( `` Bellis '' ) i `` Astranthium '' .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Entyloma , Entylomataceae , Entylomatales , Exobasidiomycetidae , Exobasidiomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1899 r. Karl Wilhelm Krieger na liściach stokrotki pospolitej w Niemczech .
Synonimy :
Charakterystyka .
Na porażonych liściach powoduje powstawanie okrągłych , prawie płaskich plam o średnicy 1–5 mm , początkowo białawych lub żółtawych , w końcu brązowych .
Wewnątrz plam tworzą się duże , gładkie , szkliste do bladożółtych zarodniki o wymiarach 9–15 µm .
Znane jest występowanie `` Entyloma bellidis '' w niektórych krajach Europy .
W Polsce pierwsze stanowisko podano w 1938 r. , następne w 1973 r .
Pasożyt jednodomowy , wąski oligofag .
Jego żywicielami są niektóre gatunki z rodziny astrowatych .
Podano jego występowanie na liściach stokrotki pospolitej ( `` Bellis perennis '' ) , `` Bellis sylvestris '' i `` Astranthium integrifolium '' .
