Entyloma calendulae
Entyloma calendulae ( Oudem . )
de Bary – gatunek podstawczaków należący do rodziny `` Entylomataceae '' .
Grzyb mikroskopijny , pasożyt niektórych roślin z rodziny astrowatych ( `` Asteraceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Entyloma , Entylomataceae , Entylomatales , Exobasidiomycetidae , Exobasidiomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1873 r. Anthonie Cornelis Oudemans nadając mu nazwę `` Protomyces calendulae '' .
Obecną nazwę nadał mu Anton de Bary w 1874 r .
Charakterystyka .
`` Entyloma calendulae '' na porażonych liściach powoduje powstawanie okrągławych plam o średnicy do 5 mm , początkowo ciemnozielonych , potem żółknących , w końcu brunatnych .
Bardzo często mają czerwoną obwódkę , pod światło wyglądają jak czerwone .
Początkowo są płaskie , w stadium dojrzałym wypukłe .
