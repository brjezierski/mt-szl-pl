Entyloma dahliae
Entyloma dahliae Syd .
& amp ; P. Syd .
– gatunek podstawczaków należący do rodziny `` Entylomataceae '' .
Grzyb mikroskopijny , pasożyt dalii , wywołujący u nich chorobę o nazwie głownia dalii .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Entyloma , Entylomataceae , Entylomatales , Exobasidiomycetidae , Exobasidiomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisali go w 1912 r. Hans Sydow i Paul Sydow .
Synonimy :
Morfologia i rozwój .
Na porażonych liściach dalii powoduje powstawanie okrągłych , eliptycznych lub kanciastych plam o średnicy do 1 cm , początkowo bladych , później brązowych i ulegających nekrozie .
Czasami sąsiednie plamy zlewają się z sobą .
W obrębie plam powstają ustilospory , kuliste do wielokątnych , szkliste , prawie bezbarwne do jasnożółtych , o średnicy 8-14 µm , z dwuwarstwową ścianką o grubości 1-3 µm , gładkie .
Konidia zwykle tworzą się na grzybni na powierzchniach plam , są szkliste , różnej wielkości i kształtu , ale najczęściej 10-25 × 2-3,3 µm .
`` Entyloma dahliae '' dokonuje infekcji roślin w glebie .
Zimują jego ustilospory w glebie na szczątkach roślin .
Nie rozprzestrzenia się przez nasiona ani bulwy , chociaż możliwe jest rozprzestrzenianie przez glebę przyczepioną do bulw .
Dojrzałe ustilospory mogą pozostawać żywotne przez długi czas po wyschnięciu .
Konidia przyczyniają się do rozprzestrzeniania się w okresie wegetacji dokonując infekcji wtórnych .
Występowanie .
Gatunek szeroko rozpowszechniony .
Występuje w Europie , w tym w Finlandii i Szwecji , Afryce , Ameryce Północnej ( USA ) , Ameryce Środkowej i Południowej ( Argentyna , Brazylia , Kolumbia , Salwador , Gwatemala , Jamajka , Wenezuela ) , Azji ( Birma , Indie , Indonezja , Izrael , Japonia , Malezja , Nepal , Pakistan , Sri Lanka , Papua Nowa Gwinea ) , Australii i Nowej Zelandii .
Występuje także w Polsce .
Pasożyt jednodomowy .
Monofag , jego żywicielami są : `` Dahlia variabilis '' , `` Dahlia coccinia '' , `` Dahlia pinnata '' i uprawiana dalia zmienna .
