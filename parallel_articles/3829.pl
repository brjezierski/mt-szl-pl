Morfologia i rozwój .
Na liściach porażonych roślin powoduje powstawanie kolistych lub kanciastych ( ograniczonych żyłami ) plam , początkowo żółtawych lub białawych , zwłaszcza na dolnej powierzchni ( barwa biała pochodzi od wytwarzanych przez grzybnię konidiów ) , później szarawych lub brązowych , o średnicy 2–5 mm .
Ustilospory kuliste do niemal kulistych , czasami wielokątne z powodu wzajemnego nacisku , szkliste , żółtawe do bladobrązowych , o średnicy 10–15 ( średnio 12,5 ) µm .
Mają dwuwarstwową ścianą o grubości 1–2 µm .
Tworzy też przetrwalniki o średnicy poniżej 1 µm i nieco grubsze egzospory , ale o jednolitej , gładkiej powierzchni .
Konidia tworzą się na pęczkach krótkich konidioforów wystających przez aparaty szparkowe .
Są bezbarwne , wrzecionowate do nitkowatych , bardzo zróżnicowanej wielkości , zwykle 15–33 × 2–3 µm , czasem do 45 µm długości .
Ustilospory zimują w zakażonych szczątkach roślin w glebie i kiełkują , głównie wiosną , infekując liście w nowym sezonie .
Konidia są przenoszone przez wiatr i deszcz i przyczyniają się do rozprzestrzeniania patogenu przez cały sezon wegetacyjny .
Porażone rośliny często mają nieco mniejsze liście i tworzą mniej kwiatów niż zdrowe .
Chociaż porażenie tym patogenem nie jest systemiczne , plamy występują prawie na każdym liściu rośliny , a choroba utrzymuje się na niewielkim obszarze rok po roku .
Występowanie i siedlisko .
Znane jest występowanie `` Entyloma ficariae '' w Ameryce Północnej , Środkowej i Południowej , Europie oraz na Nowej Zelandii .
Jest wąskim oligofagiem pasożytującym na niektórych gatunkach roślin z rodziny jaskrowatych .
W Polsce podano wiele stanowisk tego gatunku na ziarnopłonie wiosennym ( `` Ficaria verna '' ) , jaskrze rozłogowym ( `` Ranunculus repens '' ) i jaskrze jadowitym ( `` Ranunculus sceleratus '' ) .
W innych krajach podano wystąpienie `` Entyloma ficariae '' na wielu innych gatunkach jaskrów ( `` Ranunculus '' ) , a czasami także na gatunkach zawilców ( `` Anemone '' ) i rutewek ( `` Thalictrum '' ) .
