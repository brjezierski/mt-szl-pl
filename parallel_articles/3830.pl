Entyloma ranunculi-repentis
Entyloma ranunculi-repentis Sternon – gatunek podstawczaków należący do rodziny `` Entylomataceae '' .
Grzyb mikroskopijny , pasożyt niektórych roślin z rodziny jaskrowatych ( `` Ranunculaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Entyloma , Entylomataceae , Entylomatales , Exobasidiomycetidae , Exobasidiomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1925 r. Fernand Sternon .
Synonim : `` Ramularia gibba '' var .
`` ranunculi-auricomi '' Sacc .
Występowanie i siedlisko .
Znane jest występowanie `` Entyloma ficariae '' w Europie , nad Morzem Kaspijskim i na Nowej Zelandii .
Jest wąskim oligofagiem pasożytującym na niektórych gatunkach roślin z rodziny jaskrowatych .
W Polsce podano kilka stanowisk tego gatunku na różnych gatunkach `` Ranunculus '' ( jaskrów ) .
