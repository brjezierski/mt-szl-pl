Enugu ( 1961 )
Enugu ( P05 ) – nigeryjski okręt patrolowy z okresu zimnej wojny , pierwsza jednostka zbudowana dla nowo powstałej Marynarki Wojennej tego państwa .
Okręt był jednym z siedmiu pozyskanych przez Nigerię brytyjskich jednostek typu `` Ford '' .
Jednostka została zbudowana w 1961 roku w stoczni Camper and Nicholsons w Gosport i w grudniu tego roku weszła do służby .
Okręt został skreślony z listy floty w 1982 roku .
Projekt i budowa .
Okręty patrolowe typu `` Ford '' zostały zaprojektowane przez W.J .
Holta w celu zastąpienia używanych podczas II wojny światowej niewielkich patrolowców typu `` HDML '' .
Jednostki planowano wyposażyć w jednolufową wersję miotacza Squid , jednak broń ta ostatecznie nie powstała i na pokładzie umieszczono tradycyjne miotacze i zrzutnie bomb głębinowych .
