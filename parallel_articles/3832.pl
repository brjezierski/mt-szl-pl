Envigado
Envigado – miasto w zachodniej Kolumbii , w departamencie Antioquia .
Położone w Kordylierze Zachodniej ( Andy Północne ) , na południe od Medellín ( w zespole miejskim tego miasta ) .
Według spisu ludności z 30 czerwca 2018 roku miasto liczyło 215 766 mieszkańców .
W mieście rozwinięty jest przemysł spożywczy i szklarski .
Jest ośrodkiem handlowym regionu uprawy kawowca , trzciny cukrowej oraz manioku .
