Epicoccum nigrum
Epicoccum nigrum Link – gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Epicoccum , Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1815 r. Johann Heinrich Friedrich Link .
Ma ponad 20 synonimów .
Są nimi wszystkie odmiany i formy , a także m.in .
