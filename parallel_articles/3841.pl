Eppingen
Eppingen – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Heilbronn-Franken , w powiecie Heilbronn , siedziba wspólnoty administracyjnej Eppingen .
Leży nad rzeką Elsenz , ok. 20 km na zachód od Heilbronn , przy drodze krajowej B293 i linii kolejowej Heilbronn–Bruchsal .
