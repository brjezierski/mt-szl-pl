Erechim
Erechim – miasto w stanie Rio Grande do Sul w Brazylii .
Ok. 100 tysięcy mieszkańców .
Siedziba polskiego konsulatu honorowego .
Miasto powstało w 1906 na drodze kolei prowadzącej do São Paulo , założyciele to głównie włoscy imigranci .
W 1926 90 % mieszkańców stanowili Włosi pochodzący z Wenecji .
Pozostałe europejskie narodowości w mieście to Niemcy , Polacy .
Wielokulturowość miasta objawia się w jego architekturze i kulturze .
Najbardziej znaną osobą urodzoną w Erechim jest modelka Alessandra Ambrosio .
W mieście rozwinął się przemysł spożywczy .
