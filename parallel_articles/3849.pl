Erlangen
Erlangen ( ) – miasto na prawach powiatu w Niemczech , w kraju związkowym Bawaria , w rejencji Środkowa Frankonia , w regionie Industrieregion Mittelfranken .
Siedziba powiatu Erlangen-Höchstadt , mimo nieprzynależenia do powiatu .
Leży nad rzekami Schwabach i Regnitz .
Razem z miastami Norymberga i Fürth tworzy region metropolitalny Norymberga .
W mieście odbywa się doroczny festiwal piwny Bergkirchweih .
Miasto leży 280 m n.p.m .
W mieście znajduje się stacja kolejowa Erlangen .
Erlangen jest współcześnie największym miastem na terenie Nowych Czech .
Gospodarka .
W Erlangen znajdują się liczne przedsiębiorstwa wchodzące w skład koncernu elektrotechnicznego Siemens AG .
Znajduje się tu również placówka badawcza : zakłady cieplno-przepływowe firmy Areva .
Ponadto w mieście rozwinął się przemysł bawełniany , papierniczy , spożywczy oraz pszczelarski .
Edukacja .
W mieście swoją główną siedzibę ma Uniwersytet Fryderyka i Aleksandra w Erlangen i Norymberdze ( Friedrich-Alexander-Universität Erlangen-Nürnberg ) .
Jest to uczelnia partnerska Uniwersytetu Jagiellońskiego .
