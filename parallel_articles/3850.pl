Erling Haaland
Erling Braut Haaland ( do 2019 jako `` Håland '' , wym .
; ur .
21 lipca 2000 w Leeds ) – norweski piłkarz , występujący na pozycji środkowego napastnika w angielskim klubie Manchester City oraz w reprezentacji Norwegii .
Kariera klubowa .
Bryne FK .
Wychowanek akademii Bryne FK w której przygodę rozpoczął w 2012 .
W sezonach 2015 oraz 2016 Håland występował w rezerwach Bryne FK , gdzie w 14 meczach zdobył 18 bramek .
12 maja 2016 , w wieku zaledwie 15 lat , doczekał się debiutu w pierwszej drużynie Bryne w meczu OBOS-Ligaen przeciwko Ranheim Fotball .
Gdy był zawodnikiem Bryne , wyjechał na nieudane testy do drużyny niemieckiej Bundesligi – TSG 1899 Hoffenheim .
Łącznie Erling zaliczył 16 meczów dla Bryne .
