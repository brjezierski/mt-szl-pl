Ernest Mühlen
Ernest Mühlen ( ur .
8 czerwca 1926 w Ettelbruck , zm .
19 marca 2014 ) –luksemburski polityk , ekonomista i publicysta , działacz Chrześcijańsko-Społecznej Partii Ludowej , w latach 1982–1984 minister , poseł do Parlamentu Europejskiego II kadencji , parlamentarzysta krajowy .
Życiorys .
Studiował handel , finanse i ekonomię w wyższym instytucie handlowym w Antwerpii , doktoryzował się z nauk ekonomicznych na Uniwersytecie Paryskim .
W latach 1952–1973 pracował w strukturach wspólnotowych ( EWWiS i EWG ) .
Był m.in .
urzędnikiem w dziale prasowym i informacyjnym , a także kierował przedstawicielstwem Komisji Europejskiej w siedzibie Europejskiego Banku Inwestycyjnego .
Zajmował się też działalnością dydaktyczną jako wykładowca w Strasburgu , a także dziennikarstwem i publicystyką ekonomiczną .
Był długoletnim członkiem zarządu grupy Saint-Paul Luxembourg ( właściciela gazety „ Luxemburger Wort ” ) .
Uzyskał członkostwo w sekcji nauk moralnych i politycznych Institut Grand-Ducal ( luksemburskiej akademii nauk ) .
Zaangażował się w działalność polityczną w ramach Chrześcijańsko-Społecznej Partii Ludowej .
W latach 1973–1979 zasiadał w radzie miejskiej Luksemburga .
W 1979 objął stanowisko sekretarza stanu do spraw finansów .
W grudniu 1982 w rządzie Pierre ’ a Wernera przeszedł na funkcję ministra rolnictwa , winogrodnictwa , gospodarki wodnej i leśnictwa , którą pełnił do lipca 1984 .
Od 1979 do 1984 był też przedstawicielem Luksemburga w Banku Światowym .
W latach 1984–1989 sprawował mandat posła do Parlamentu Europejskiego II kadencji .
Zasiadał we frakcji Europejskiej Partii Ludowej , w latach 1985–1986 wchodził w skład jej prezydium .
Pracował w Komisji ds .
Stosunków Gospodarczych z Zagranicą , a później w Komisji ds .
Gospodarczych i Walutowych oraz Polityki Przemysłowej .
W latach 1989–1991 zasiadał w Izbie Deputowanych , po czym do 1996 reprezentował Luksemburg w radzie dyrektorów Europejskiego Banku Odbudowy i Rozwoju .
