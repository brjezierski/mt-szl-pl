Che Guevara
Che Guevara , właśc .
Ernesto Guevara , pseudonim Fernando Sacamuelas ( ur .
14 czerwca 1928 w Rosario , zm .
9 października 1967 w La Higuera ) – latynoamerykański rewolucjonista , pisarz , z zawodu lekarz .
Urodził się w Argentynie .
W latach 50 .
XX w. jeden z przywódców rewolucji kubańskiej .
Po jej sukcesie wstąpił w skład rewolucyjnego rządu .
W 1965 roku brał udział w walkach partyzanckich w Kongu .
Od 1966 roku organizował oddziały partyzanckie w Boliwii .
Tam został schwytany i zabity .
Jako ideolog i pisarz stworzył własną oryginalną wersję wojny partyzanckiej ( foco guerrillero ) prowadzonej przez chłopów .
Jego poglądy określa się mianem guevaryzmu .
Przeszedł do historii jako idol zrewoltowanej młodzieży .
Życiorys .
Dzieciństwo i edukacja .
Urodził się w argentyńskim Rosario w 1928 roku .
Pochodził z rodziny wywodzącej się z klasy średniej .
Jego rodzina była pochodzenia hiszpańsko-irlandzkiego ( syn Celii de la Serny i Ernesto Guevary Lyncha ) i wykazywała lewicowe sympatie polityczne .
W dzieciństwie chorował na astmę .
Naukę rozpoczął w marcu 1937 roku .
Uczęszczał do szkoły podstawowej w mieście San Martín .
W marcu 1942 roku trafił do szkoły średniej .
Latem 1943 roku rodzina Guevarów przeprowadziła się do Córdoby .
W liceum na własną rękę uczył się filozofii .
Jako nastolatek przyjął lewicowe poglądy i zasilił grupę opozycyjną względem prezydenta Juana Peróna .
Po ukończeniu osiemnastego roku życia rozpoczął pracę w laboratorium Dirección provincial de Vialidad .
Pracę porzucił , gdy dowiedział się o wylewie babci .
Towarzyszył jej w ostatnich dniach jej życia .
Następnie złożył podanie o przyjęcie na wydział medycyny na uniwersytecie w Buenos Aires .
W trakcie studiów dorabiał , pracując w między innymi Clínica Pisani oraz podróżował po kraju na motocyklu .
Uniknął służby wojskowej , ponieważ komisja wojskowa zanegowała jego zdolność do służby z powodu „ ograniczonej wydolności fizycznej ” .
Na emigracji .
Pobyt w Gwatemali .
Po ukończeniu studiów , 7 lipca 1953 wyjechał z kraju .
Odbył podróż motocyklową po Boliwii , Peru , Ekwadorze , Panamie , Kostaryce , Hondurasie i Salwadorze .
W krajach tych natknął się na problem biedy i ucisku społecznego .
W wyprawach towarzyszył mu jego przyjaciel Alberto Granado .
