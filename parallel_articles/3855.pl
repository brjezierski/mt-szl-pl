Ernő Rubik
Ernő Rubik ( ur .
13 lipca 1944 w Budapeszcie ) – węgierski architekt i rzeźbiarz .
W 1974 wynalazł zabawkę logiczną nazwaną od jego nazwiska kostką Rubika ( węg .
`` bűvös kocka '' , magiczna kostka ) .
Życiorys .
Ojciec Rubika był inżynierem lotnictwa pracującym w fabryce samolotów w Ostrzyhomiu , matka była poetką .
Ukończył studia na kierunku architektura na Politechnice w Budapeszcie ( `` Műszaki Egyetem '' ) w 1967 i pozostał na uczelni na studiach podyplomowych z rzeźbiarstwa .
W latach 1971–1975 pracował jako architekt .
Całe swoje dotychczasowe życie spędził na Węgrzech .
We wczesnych latach 80 .
XX wieku został redaktorem magazynu `` ... És játék '' , poświęconego grom i układankom .
W 1983 założył Rubik Stúdió , gdzie projektował meble i gry .
W 1987 został profesorem na stałym etacie , a w 1990 prezesem Węgierskiej Akademii Inżynierskiej ( `` Magyar Mérnöki Akadémia '' ) , gdzie stworzył organizację International Rubik Foundation , wspierającą szczególnie utalentowanych młodych inżynierów i projektantów .
Obecnie zajmuje się głównie rozwojem gier wideo i sprawami architektury i prowadzi Rubik Stúdió .
Jest osobą introwertyczną i trudno dostępną .
Z reguły odrzuca propozycje udziału w zawodach w układaniu kostki Rubika na czas , chociaż był obecny na Mistrzostwach Świata w 2007 roku w Budapeszcie .
W 2014 otrzymał najwyższe węgierskie odznaczenie : Wielki Krzyż Orderu Świętego Stefana .
