Ersekë
Ersekë , Erseka – miasto w południowo-wschodniej Albanii , stolica okręgu Kolonjë w obwodzie Korcza .
Siedziba gminy miejskiej Ersekë .
Prawa miejskie uzyskało w 1958 .
Liczba mieszkańców wynosi około 7750 ( 2005 ) .
Położone w górach Gramos na wysokości 1 050 m , najwyżej położone miasto Albanii .
Historia .
Założone w XVII wieku .
Pierwszymi mieszkańcami regionu Kolonjë byli ludzie ze starogreckiego plemiona Deksaroi , które mieszkało w Epirze , na granicy Ilirii .
