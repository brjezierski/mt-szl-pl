Erysiphe adunca
Erysiphe adunca ( Wallr . )
Fr .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt wywołujący u wierzb i topoli chorobę o nazwie mączniak prawdziwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1819 r. Carl Friedrich Wallroth nadając mu nazwę `` Alphitomorpha adunca '' .
Lektotypem była topola czarna ( `` Populus nigra '' ) .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1829 r. Elias Fries .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Grzybnia rozwija się na obydwu powierzchniach liścia w postaci oddzielnych kolonii , czasami zlewających się z sobą i obejmujących całą powierzchnię liścia .
Wygląda jak biały mączysty nalot i jest cienka lub średnio gruba .
