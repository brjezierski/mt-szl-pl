Morfologia .
Na liściach porażonych roślin tworzy trwałą , białą grzybnię powietrzną , często zajmującą całą powierzchnię liścia .
Grzybnia rozwija się tylko na powierzchni skórki liścia , zapuszczając do komórek skórki ssawki .
Nie wnikają one jednak głębiej , niż do skórki .
W grzybni na powierzchni liścia tworzą się bardzo liczne , wyprostowane , cylindryczne konidiofory o długości 15-30 μm i szerokości 6-9 μm , złożone z komórki podstawowej i 1-3 mniejszych komórek .
