Erysiphe alphitoides
Erysiphe alphitoides ( Griffon & amp ; Maubl . )
U. Braun & amp ; S. Takam .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny będący pasożytem bezwzględnym roślin , głównie dębów
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1912 r. Édouard Griffon i André Maublanc nadając mu nazwę `` Microsphaera alphitoides '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 r. U. Braun i S. Takam , przenosząc go do rodzaju `` Erysiphe '' .
