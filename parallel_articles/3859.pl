Golovinomyces artemisiae
Golovinomyces artemisiae ( Grev . )
V.P .
Heluta – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Golovinomyces , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1824 roku Robert Kaye Greville nadając mu nazwę `` Erysiphe artemisiae '' Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1988 roku V.P .
Heluta , przenosząc go do rodzaju `` Golovinomyces '' .
Morfologia .
Grzyb mikroskopijny .
Grzybnia rozwija się głównie na powierzchni liści , łodyg i kwiatostanów roślin , do wnętrza tkanek zapuszcza tylko ssawki .
Występuje zazwyczaj na obydwu powierzchniach liścia , czasami tylko na dolnej powierzchni .
