Erysiphe azaleae
Erysiphe azaleae ( U. Braun ) U. Braun & amp ; S. Takam .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt bezwzględny rozwijający się na niektórych gatunkach różaneczników .
Wywołuje u nich chorobę o nazwie mączniak prawdziwy azalii .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1982 r. U. Braun nadając mu nazwę `` Microsphaera azaleae '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 r. U. Braun i S. Takam .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Biała lub białoszara grzybnia rozwija się na obu powierzchniach liści , oraz na pąkach i owocach .
Do wnętrza tkanek zapuszcza tylko ssawki .
Wygląda jak mączysty nalot i jest w zależności od stopnia rozwoju gęsta , zwarta , lub rozproszona , rzadka .
