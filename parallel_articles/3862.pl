Erysiphe catalpae
Erysiphe catalpae Simonyan – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny rozwijający się na roślinach z rodzaju surmia ( `` Catalpa '' ) .
Występuje w Europie i w rozproszeniu w wielu krajach Europy , w tym w Polsce .
U porażonych roślin powoduje chorobę zwaną mączniakiem prawdziwym .
Jej objawy to okrągłe lub nieregularne białe plamy po obu stronach liści i na zielonych pędach .
W ich obrębie następuje obficie rozwój grzybni .
Przy dużym natężeniu choroby następuje osłabienie wzrostu i przedwczesna utrata liści , co powoduje obniżoną wartość estetyczną roślin .
Stwierdzono występowanie `` Erysiphe catalpae '' na następujących gatunkach roślin : surmia bignoniowa , surmia żółtokwiatowa , surmia wielkokwiatowa i `` Catalpa japonica '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson został zdiagnozowany na surmii bignoniowej ( `` Catalpa bignonioides '' ) w Armenii w 1984 r. Jest ona holotypem tego gatunku .
