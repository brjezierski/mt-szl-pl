Erysiphe convolvuli
Erysiphe convolvuli DC .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt bezwzględny rozwijający się na roślinach należących do rodzajów powój ( `` Convolvulus '' ) i kielisznik ( `` Calystegia '' ) .
Wywołuje u nich chorobę o nazwie mączniak prawdziwy .
