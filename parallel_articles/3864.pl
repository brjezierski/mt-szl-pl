Erysiphe cruciferarum
Erysiphe cruciferarum Opiz ex L. Junell – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Wywołuje chorobę o nazwie mączniak prawdziwy roślin kapustnych .
Systematyka i nazewnictwo .
