Erysiphe hyperici ( Wallr . )
S. Blumer – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt , fitopatogen występujący na różnych gatunkach roślin z rodzaju dziurawiec ( `` Hypericium '' ) .
Wywołuje u nich chorobę o nazwie mączniak prawdziwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1819 roku Carl Friedrich Wallroth nadając mu nazwę `` Alphitomorpha hyperici '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1933 roku Samuel von Blumer , przenosząc go do rodzaju `` Erysiphe '' .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
