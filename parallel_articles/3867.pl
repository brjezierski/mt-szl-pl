Erysiphe knautiae
Erysiphe knautiae Duby – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Wśród roślin uprawianych w Polsce wywołuje chorobę mączniak prawdziwy skabiozy ( driakwi ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go 1830 r. Jean Étienne Duby na świerzbnicy polnej .
Morfologia .
Grzybnia tworzy rozproszone plamy w postaci białego nalotu na obydwu stronach liści .
Konidiofory wzniesione , cylindryczne .
