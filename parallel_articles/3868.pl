Erysiphe lonicerae
Erysiphe lonicerae DC .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt różnych gatunków wiciokrzewów ( `` Lonicera '' ) oraz bzu czarnego ( `` Sambucus nigra '' ) .
Wywołuje u nich chorobę zwaną mączniakiem prawdziwym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Holotyp : na `` Lonicera caprifolium '' , Francja .
Występowanie .
Znane jest występowanie `` E. lonicera '' głównie w Europie .
Poza nią notowany jest w Ameryce Północnej ( USA ) , Japonii i na Nowej Zelandii .
W piśmiennictwie naukowym na terenie Polski do 2003 r. dla synonimu `` Microsphaera lonicerae '' podano kilka stanowisk na wiciokrzewie tatarskim ( `` Lonicera tatarica '' ) , w. przewiercieniu ( `` Lonicera caprifolium '' ) , w. pomorskim ( `` Lonicera periclymenum '' ) i w. pospolitym ( `` Lonicera xylosteum '' ) .
Gatunki podobne .
W 1960 r. na w Pakistanie opisano nowy gatunek mączniaka – `` Erysiphe sambuci '' S. Ahmad .
Jak dotąd jednak w Polsce nie potwierdzono jego występowania .
