Golovinomyces magnicellulatus
Golovinomyces magnicellulatus ( U. Braun ) V.P .
Heluta – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Colovinomyces , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1978 roku Uwe Braun nadając mu nazwę `` Erysiphe magnicellulata . ''
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1988 roku V.P .
Heluta , przenosząc go do rodzaju `` Golovinomyces '' .
