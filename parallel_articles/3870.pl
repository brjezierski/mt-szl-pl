Erysiphe necator
Erysiphe necator Schwein .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy nazwy naukowej :
Dawniej stadium konidialne `` Erysiphe necator '' opisano jako odrębny gatunek – `` Oidium tuckeri '' .
Obecnie wiadomo już , że jest to tylko anamorfa .
Morfologia .
Jest to grzyb mikroskopijny .
Jego grzybnia rozwija się tylko na powierzchni zaatakowanych roślin , na liściach obustronnnie , zarówno na górnej , jak i dolnej powierzchni .
Tworzy bardzo cienką warstwę w postaci pojedynczych ognisk , potem rozlewających się w plamy , z czasem pokrywające całą powierzchnię .
Strzępki kolankowato powyginane , i charakterystycznie poskręcane , o długości 25–160 μm i szerokości 5–9 μm .
Wyrastają z nich krótkie i klapowane ssawki wnikające do komórek epidermy gospodarza .
Konidiofory wyprostowane , o długości 0,04–0,4 mm .
