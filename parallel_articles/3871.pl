Erysiphe pisi
Erysiphe pisi DC .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny pasożytujący na niektórych roślinach z rodziny bobowatych .
U grochu ( `` Pisum '' ) wywołuje chorobę o nazwie mączniak prawdziwy grochu .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Grzybnia rozwija się na obydwu powierzchniach liści , łodyg i strąkach roślin , do wnętrza tkanek zapuszcza tylko ssawki .
Wygląda jak biały , mączysty nalot , ale jest bardzo zmienna jeśli chodzi o gęstość .
Rzadko bywa gęsta , wojłokowata .
Strzępki o szerokości 5-7,5 μm , ssawki o średnicy około 17–25 μm .
Konidiofory proste , nierozgałęzione , wyprostowane .
Ich pierwsza komórka ma rozmiary ( 15– ) 20–50 ( –70 ) × 6–10 μm .
