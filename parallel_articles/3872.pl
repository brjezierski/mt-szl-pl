Erysiphe prunastri
Erysiphe prunastri DC .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt bezwzględny rozwijający się na roślinach należących do rodzaju śliwa ( `` Prunus '' ) .
Wywołuje u nich chorobę o nazwie mączniak prawdziwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Lektotyp : na `` Prunus spinosa '' .
Morfologia .
Grzyb mikroskopijny .
Biała lub białoszara grzybnia rozwija się na obu powierzchniach liści , ale głównie na górnej ich powierzchni .
Do wnętrza tkanek zapuszcza tylko ssawki .
Wygląda jak mączysty nalot i jest w zależności od stopnia rozwoju rozproszona , rzadka , lub gęsta i zwarta .
Wytwarza rozproszone , lub zgrupowane , kuliste klejstotecja o średnicy ( 80– ) 85–125 ( –150 ) μm .
Zbudowane są z nieregularnych , wielokątnych , ciemnych komórek o średnicy około 8–20 μm średnicy .
Przyczepki w liczbie 15–50 , ale najczęściej 20–30 , wyrastające głównie w równikowej części klejstotecjów .
Są hialinowe , bez przegród , gładkie , o szerokości około 4–6,5 μm .
Są nierozgałęzione , mają długość równą 1–2 średnic klejstotecjum i haczykowato zagięte końce .
W każdym klejstotecjum znajduje się 5–18 , najczęściej 10–15 siedzących lub na krótkich trzonkach worków o rozmiarach ( 40– ) 45–60 ( –70 ) × ( 20– ) 25–35 ( –40 ) μm .
W każdym worku powstaje 4–8 , przeważnie 5–7 elipsoidalnych askospor o rozmiarach 13–20 × 8-12 μm .
Występowanie .
Występuje w całej Europie i Azji ( po Kazachstan , Kirgizję , Turkmenię i Armenię ) .
Opisano występowanie na następujących gatunkach : `` Prunus cerasifera '' , `` Prunus dasyphylla '' , `` Prunus domestica '' , `` Prunus insititia '' , `` Prunus japonica '' , `` Prunus mahaleb '' , `` Prunus padus '' , `` Prunus spinosa '' .
