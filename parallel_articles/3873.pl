Morfologia .
Grzyb mikroskopijny .
Biała lub białoszara grzybnia rozwija się głównie na górnej powierzchni liści , oraz na pąkach .
Początkowo tworzy delikatny rzadki , rozproszony nalot , z czasem gęstniejący .
