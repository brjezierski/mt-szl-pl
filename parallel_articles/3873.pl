Erysiphe syringae
Erysiphe syringae Schwein .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Pasożyt bezwzględny rozwijający się na niektórych gatunkach roślin z rodziny oliwkowatych ( `` Oleaceae '' ) .
