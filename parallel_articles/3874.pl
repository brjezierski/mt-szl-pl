Erysiphe tortilis
Erysiphe tortilis ( Wallr . )
Link – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' .
Grzyb mikroskopijny pasożytujący na dereniach ( `` Cornus '' ) .
Wywołuje u nich chorobę o nazwie mączniak prawdziwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1819 r. Carl Friedrich Wilhelm Wallroth nadając mu nazwę `` Alphitomorpha tortilis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1824 r. Johann Heinrich Friedrich Link .
Synonimy :
Morfologia .
Grzybnia tworzy rozproszone plamy w postaci białego nalotu , głównie na górnej stronie liści .
