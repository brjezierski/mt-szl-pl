Erysiphe trifolii
Erysiphe trifolii Grev .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' .
Wywołuje chorobę o nazwie mączniak prawdziwy koniczyny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Grzybnia rozwija się głównie na powierzchni liści , łodyg i kwiatostanów roślin , do wnętrza tkanek zapuszcza tylko ssawki .
Wygląda jak biały , mączysty nalot , Na powierzchni liścia tworzy rozproszone plamki lub duże plamy , czasami zajmujące nawet cały liść .
Strzępki mają szerokość 2,5–7,5 μm .
Pierwszy człon konidioforów cylindryczny , zazwyczaj prosty , czasami zakrzywiony lub powyginany .
Ma długość ( 15– ) 25–38 ( –55 ) × 6,5–9 μm .
Dalsze dwa człony krótsze , lub o tej samej długości .
