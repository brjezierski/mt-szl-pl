Erytropoetyna
Erytropoetyna ( skrót EPO , ATC : B 03 XA 01 ) – glikoproteinowy hormon peptydowy , którego główną funkcją jest stymulacja różnych etapów erytropoezy .
Zwiększa liczbę erytrocytów , stężenie hemoglobiny i liczbę retikulocytów .
Nie ma wpływu na powstawanie krwinek białych , nie działa cytotoksycznie na szpik kostny .
Erytropoetyna jest wydalana z moczem .
Erytropoetyna produkowana jest w wątrobie i korze nerek .
U zwierząt gen odpowiedzialny za wytwarzanie erytropoetyny w nerkach zlokalizowano w komórkach interstycjalnych .
Wywiera swoje działanie poprzez połączenie ze specyficznym receptorem erytropoetynowym ( EpoR ) .
W ten sposób wpływa na komórki macierzyste szpiku kostnego , zwiększając produkcję prekursorów komórek szeregu czerwonokrwinkowego , a zatem zwiększając wytwarzanie erytrocytów .
