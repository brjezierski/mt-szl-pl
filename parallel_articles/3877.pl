Erytrea
Erytrea , oficjalnie Państwo Erytrea ( tigrinia ኤርትራ – `` Ertra '' , ሃግሬ ኤርትራ – `` Hagere Ertra '' ; arab .
إرتريا – `` Iritrija '' , دولة إرتريا – `` Daulat Iritrija '' ) – państwo w Afryce Wschodniej , położone nad Morzem Czerwonym .
Graniczy z Etiopią ( 912 km ) , Dżibuti ( 109 km ) i Sudanem ( 605 km ) .
Erytrea liczy 121,1 tys .
km² powierzchni .
W 2013 roku w Erytrei mieszkało 5,7 mln mieszkańców .
Gęstość zaludnienia wynosi 47 osób na kilometr kwadratowy .
W państwie obowiązują dwa języki urzędowe : arabski oraz tigrinia .
W użyciu jest także język angielski .
Stolicą Erytrei jest Asmara .
W kraju obowiązuje waluta nakfa .
Państwo dzieli się na sześć regionów .
Kraj uzyskał niepodległość 24 maja 1993 roku .
Erytrea należy do Międzynarodowej Agencji Energii Atomowej , Międzynarodowej Organizacji Pracy , Międzynarodowego Banku Odbudowy i Rozwoju , Międzynarodowego Funduszu Walutowego , Organizacji Narodów Zjednoczonych oraz Unii Afrykańskiej .
Geografia .
Erytrea zajmuje północną część Wyżyny Abisyńskiej .
Najwyższym szczytem jest Soira ( według różnych źródeł Soira mierzy 3013 m lub 3018 m n.p.m . ) .
Rozciągłość z północy na południe wynosi 950 km .
Linia brzegowa jest słabo rozwinięta , w środkowej części Erytrei znajdują się głęboko wcięte w ląd zatoki .
Przy zatokach znajdują się liczne wysepki oraz rafy koralowe ( największy archipelag – Dahlak ) .
Przybrzeżne niziny mają klimat zwrotnikowy suchy ( roczne opady poniżej 200 mm ) .
Niziny te są jednym z najgorętszych miejsc na Ziemi ( średnia temperatura roczna wynosi 30 stopni Celsjusza ) .
Na Wyżynie Abisyńskiej klimat jest bardziej wilgotny ( roczne opady ok. 600 mm ) i chłodniejszy .
Erytrea jest podzielona na 6 regionów administracyjnych ( `` zobas '' ) , które z kolei są podzielone na dystrykty ( `` sub-zobas '' ) .
Demografia .
Społeczeństwo Erytrei jest zróżnicowane .
Tigrajczycy i ludy Tigre stanowią 81,4 % mieszkańców , Afarowie – 5 % , Saho – 5 % , Bedża – 2,5 % , Bilen – 2,1 % , Kunama – 2 % , Nara – 1,5 % , Rashaida – 0,5 % .
Ludność posługuje się językami semickimi z grupy etiopskiej ( tigrinia , tigre ) i kuszyckimi ( afar ) .
Ważniejsze miasta to : Asmara , Massaua , Assab .
Przyrost naturalny jest bardzo wysoki .
W 2004 roku wyniósł 12,89‰ , a w 2006 wzrósł do 24,9‰ W 2011 roku przyrost wyniósł 25‰ .
W 2006 roku analfabetyzm wyniósł 44 % .
Średnia długość życia wyniosła : 56 lat ( mężczyźni ) , 60 lat ( kobiety ) .
Od uzyskania przez Erytreę niepodległości z państwa uciekło dziesięć procent mieszkańców .
Ustrój polityczny .
Głową państwa oraz szefem rządu jest prezydent wybrany przez parlament .
Władza ustawodawcza należy do jednoizbowego parlamentu ( Zgromadzenia Narodowego ) .
W skład parlamentu wchodzi 150 osób .
75 członków pochodzi z Centralnej Rady Ludowego Frontu na rzecz Demokracji i Sprawiedliwości , pozostali są deputowanymi wybrani przez komitety regionalne Ludowego Frontu .
11 miejsc jest zarezerwowanych dla kobiet .
Przewodniczącym Zgromadzenia Narodowego jest prezydent Erytrei .
Władzę wykonawczą sprawuje rząd ( Rada Państwowa ) , którą mianuje i kieruje prezydent .
Konstytucję uchwalono w 1997 roku , choć do dzisiaj nie weszła ona w życie .
Jedyną legalną partią w Erytrei jest Ludowy Front na rzecz Demokracji i Sprawiedliwości .
Parlament po raz ostatni zebrał się w 2002 roku .
Prawa człowieka .
W Erytrei nie przestrzega się praw człowieka .
Według działaczy praw człowieka , Erytrea jest państwem totalitarnym .
W 2017 Erytrea znalazła się na 179. miejscu w rankingu organizacji obrony wolności prasy „ Reporterzy bez Granic ” , dotyczącego wolności mediów .
W kraju nie ma niezależnych sądów ani mediów .
