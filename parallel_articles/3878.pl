Erywań ( stacja kolejowa )
Erywań – stacja kolejowa w Erywaniu , w Armenii .
Jest największą stacją kolejowa w kraju .
Posiada 2 perony .
Znajduje się tu stacja metra .
Historia .
W 1902 roku otwarto stację w Erywaniu z okazji otwarcia pierwszej linii kolejowej łączącej Alexandroupolis ( Giumri ) i Tbilisi , podczas gdy drugą linię otwarto do azerskiego miasta Culfa i Iranu w 1908 roku .
Budynek dworca został ukończony w roku 1956 .
W dniu 31 lipca 2009 roku otwarto Muzeum Transportu Kolejowego Armenii , a w 2010 miała miejsce przebudowa dworca .
