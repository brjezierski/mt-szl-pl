Erzurum
Erzurum ( orm .
Կարին – `` Karin '' , gr .
Θεοδοσιούπολις – `` Theodosioupolis '' ) – miasto we wschodniej Turcji , na Wyżynie Armeńskiej , ośrodek administracyjny i stolica prowincji Erzurum .
Według ostatniego spisu ludności miasto jest zamieszkiwane przez około 361 tysięcy mieszkańców .
W 2021 mieszka w nim 756 tys .
