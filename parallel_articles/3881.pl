Esbjerg ( stacja kolejowa )
Esbjerg - stacja kolejowa w Esbjerg , w Danii .
Znajdują się tu 2 perony .
Stację otwarto w 1874 z instalacją portu Esbjerg .
Obecny budynek został zaprojektowany przez Heinricha Wencka i został otwarty w 1904 roku .
W 2008 istnieje kilka tras i operatorów , na przykład : DSB i Arriva .
