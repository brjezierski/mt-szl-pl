Esch-sur-Alzette
Esch-sur-Alzette ( luks .
Esch-Uelzecht ; niem .
Esch an der Alzette ) – gmina ze statusem miejskim w południowo-zachodnim Luksemburgu , w dystrykcie Luksemburg , stolica administracyjna kantonu Esch-sur-Alzette .
Jest drugim pod względem wielkości miastem w Luksemburgu oraz cechuje się największą gęstością zaludnienia .
Graniczy z Francją .
W 2005 roku miasto Esch-sur-Alzette , które stanowi centrum gminy , zamieszkiwało 27 146 osób .
Miasto jest centrum krajowej produkcji stali .
Jednym z największych pracodawców jest firma Arcelor , największy światowy producent stali , powstały z połączenia firm Aceralia , Arbed i Usinor .
Historia .
Miasto Esch zostało po raz pierwszy wymienione w dokumentach pisanych w bulli papieża Honoriusza II z 12 kwietnia 1128 roku pod nazwą `` Asch '' .
W 1328 roku Jan Ślepy nadał Esch status „ wolnego miasta ” .
