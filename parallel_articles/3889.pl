Eskişehir
Eskişehir ( wymowa turecka IPA [ esˈciʃehiɾ ] , łac .
`` Dorylaeum '' , gr .
Δορύλαιον , `` Dorýlaion '' ) – miasto w północno-zachodniej Turcji ( region Środkowa Anatolia ) nad rzeką Porsuk , stolica prowincji o tej samej nazwie , ośrodek akademicki ( Osmangazi Üniversitesi i Uniwersytet Anadolu – jeden z największych na świecie ) .
Według danych z 2009 roku miasto zamieszkiwało 617 215 osób , z czego 305 999 stanowili mężczyźni , a 311 216 kobiety .
Eskişehir znany jest z produkcji chałwy ( między innymi gatunków `` met helvası '' i `` nuga helva '' ) , czebureków oraz wydobywania sepiolitu .
Na pobliskich wzgórzach można znaleźć gorące źródła .
Miasto znajduje się na 250 km na zachód od Ankary , 350 km na południowy wschód od Stambułu i 90 km na północny wschód od Kütahya .
Prowincja obejmuje obszar 2678 km2 .
