Esparron-de-Verdon
Esparron-de-Verdon – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 290 osób , a gęstość zaludnienia wynosiła 8 osób/km² .
W styczniu 2015 r. Esparron-de-Verdon zamieszkiwało 430 osób , przy gęstości zaludnienia wynoszącej 11,9 osób/km² .
