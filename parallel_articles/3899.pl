Essam El-Hadary
Essam Kamal Tawfik El-Hadary ( arab .
عصام الحضري , ur .
15 stycznia 1973 w Damietcie ) – piłkarz egipski grający na pozycji bramkarza .
Od 2018 występuje w Ismaily .
Kariera klubowa .
El-Hadary wychował się w klubie Damietta Club , gdzie występował do 1996 roku .
Wtedy też przeszedł do Al-Ahly Kair , jednego z czołowych klubów w kraju , gdzie już na samym początku został pierwszym bramkarzem .
Już w 1997 roku wywalczył swój pierwszy tytuł mistrza kraju , a następnie aż do 2000 roku jeszcze trzykrotnie z rzędu osiągnął ten sukces .
