Esterpole
Esterpole – wieś w Polsce położona w województwie wielkopolskim , w powiecie śremskim , w gminie Brodnica .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
Wieś położona przy drodze powiatowej nr 4061 z Żabna do Ludwikowa .
Nazwa wsi Esterpole pochodzi od imienia żony Józefa Wybickiego Estery .
Wcześniejsza nazwa wsi brzmiała Przylepskie Oręby .
