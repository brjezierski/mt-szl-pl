Estuaire
Estuaire – jedna z dziewięciu prowincji Gabonu .
Sąsiaduje z prowincjami : Woleu-Ntem , Ogowe Środkowe i Ogowe Nadmorskie oraz z Gwineą Równikową .
Stolicą prowincji jest Libreville , które jednocześnie jest stolicą Gabonu .
Nazwa wzięła się od położonego na terenie prowincji Estuaire du Gabon – wspólnego estuarium rzek Komo i Ebe .
