Estonia
Estonia ( ) , Republika Estońska ( est .
`` Eesti Vabariik '' ) – państwo unitarne w Europie Północnej , nad Morzem Bałtyckim , nad Zatoką Fińską , nad którą leży Tallinn , stolica i największe miasto kraju , i Zatoką Ryską .
Powstało po I wojnie światowej .
Członek Unii Europejskiej i NATO .
Graniczy z Łotwą od południa i z Rosją od wschodu oraz z Finlandią przez Zatokę Fińską .
Jedno z najbardziej zinformatyzowanych państw świata .
Nazwa .
Nazwa `` Estonia '' ( we współczesnym języku estońskim `` Eesti '' ) może być wywodzona ze słowa `` Aestii '' , zlatynizowanej nazwy nadanej przez starożytnych Germanów ludom zamieszkującym tereny na północny wschód od Wisły .
Rzymski historyk Tacyt w 98 r. n.e .
po raz pierwszy pisał o ludach `` Aestii '' i wczesnych Skandynawach .
Dawne skandynawskie sagi nazywają ziemie na południe od Zatoki Fińskiej `` Eistland '' .
Tożsamość .
Estonia a Skandynawia .
Estonia ma silne wpływy skandynawskie , szczególnie szwedzkie i duńskie .
Sama nazwa stolicy Estonii – Tallinn znaczy „ Duńskie Miasto ” .
Po uzyskaniu przez Estonię niepodległości w 1918 roku rząd estoński rozpoczął starania o przyjęcie do kręgu krajów nordyckich .
Osobą szczególnie zainteresowaną włączeniem Estonii do tej grupy był Aleksander Kesküla .
Próby te zostały zahamowane po drugiej wojnie światowej , po aneksji kraju przez ZSRR .
Po odzyskaniu niepodległości w 1991 r. powrócono do idei identyfikacji z kulturą nordycką .
Kluczowym momentem była przemowa estońskiego ministra spraw zagranicznych Toomasa Hendrika Ilvesa w Szwedzkim Instytucie Spraw Zagranicznych w 1999 r .
