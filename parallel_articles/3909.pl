Eswatini
Eswatini , dawniej : `` Suazi '' , `` Ngwane '' ( Królestwo Eswatini , dawniej : Królestwo Suazi , Królestwo Ngwane ; ang .
`` Eswatini '' , `` Kingdom of Eswatini '' , dawniej : `` Swaziland '' , `` Kingdom of Swaziland '' , `` Ngwane '' , `` Kingdom of Ngwane '' ; suazi : `` eSwatini '' , `` Umbuso weSwatini '' ) – niewielkie śródlądowe państwo w południowej Afryce ( jedno z najmniejszych na kontynencie ) , graniczące z Mozambikiem i Południową Afryką .
Siedzibą parlamentu i króla jest Lobamba .
Geografia .
Eswatini oferuje dużą różnorodność krajobrazów , od gór wzdłuż granicy z Mozambikiem , przez sawanny na wschodzie , po lasy deszczowe na północnym zachodzie .
Główną rzeką płynącą przez ten kraj jest Lusutfu .
Stolica państwa , Mbabane , jest drugim co do wielkości miastem Eswatini .
Inne większe ośrodki to Manzini , Lobamba i Siteki .
Klimat jest zwrotnikowy , wilgotny .
Wyraźnie widoczna jest pora deszczowa .
Trwa ona od września do października i od marca do kwietnia .
Historia .
Na tereny obecnego Eswatini prawdopodobnie w I tysiącleciu przed naszą erą przybyli Buszmeni .
Zostali oni wyparci przez ludy Bantu w VII wieku naszej ery .
