Etiopia
Etiopia ( , trl .
Ityopp ’ ya ) , oficjalnie Federalna Demokratyczna Republika Etiopii ( , trl .
yeʾĪtiyoṗṗya Fēdēralawī Dēmokirasīyawī Rīpebilīk ) , dawniej Abisynia – państwo położone we wschodniej Afryce .
Od południa graniczy z Kenią , na zachodzie z Sudanem i Sudanem Południowym , na wschodzie z Dżibuti i Somalią , a na północnym wschodzie z Erytreą .
Geografia .
Etiopia położona jest we wschodniej części kontynentu afrykańskiego , w tzw .
Rogu Afryki .
Większość kraju obejmuje wysoką Wyżynę Abisyńską , mniejsza , wschodnia leży na niższej Wyżynie Somalijskiej .
Wyżyny te oddziela południkowo Rów Abisyński – rów tektoniczny będący odcinkiem Wielkich Rowów Afrykańskich , granicy płyt tektonicznych .
