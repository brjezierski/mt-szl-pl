Etterbeek
Etterbeek – jedna z 19 gmin miejskich w Belgii , w Regionie Stołecznym Brukseli licząca około 42 tys .
mieszkańców ( 2006 ) .
Znajduje się w środkowo-wschodniej części regionu i sąsiaduje z gminą Bruksela .
Ośrodek przemysłu spożywczego oraz włókienniczego .
Na terenie gminy znajduje się stacja kolejowa – Merode ( stacja kolejowa Etterbeek położona jest w sąsiedniej gminie Ixelles ) oraz trzy stacje metra ( Merode , Thieffry , Pétillon – linie 1 i 5 ) .
Gminę zamieszkują liczni Polacy .
Znajdują się tutaj : konsulat generalny oraz kościół , w którym odbywają się polskie msze , a także co najmniej trzy polskie sklepy .
Pomiędzy stacjami metra Schuman i Merode rozciąga się wielki Park Pięćdziesięciolecia ( `` Parc du Cinquantenaire / Jubelpark '' ) , na terenie którego znajduje się łuk triumfalny , muzeum wojskowe , muzeum samochodowe , muzeum sztuki , a także Wielki Meczet Brukseli .
Niekwestionowanym centrum Etterbeeku jest Merode , gdzie znajdują się liczne restauracje , puby i sklepy .
