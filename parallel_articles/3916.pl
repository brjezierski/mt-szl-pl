Biografia .
Marie-Joseph Sue urodził się w Paryżu 26 stycznia 1804 roku .
Sławę przyniosły mu , publikowane początkowo w „ Le Journal des débats ” , sensacyjne powieści z życia najuboższej ludności Paryża , zawierające krytykę istniejącego systemu społecznego .
Wywarły one wpływ na nastroje polityczne w przededniu rewolucji 1848 roku ( Wiosny Ludów ) .
Pisał również powieści marynistyczne , historyczne i obyczajowe .
Zmarł w Annecy-le-Vieux 3 sierpnia 1857 roku .
Odznaczony 10 marca 1839 roku orderem Legii Honorowej wraz z Alfredem de Mussetem i Fryderykiem Soulié .
