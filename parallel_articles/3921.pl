Aspergillus amstelodami
Aspergillus amsterodami ( L. Mangin ) Thom & amp ; Church – gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Teleomorfę tego taksonu po raz pierwszy opisał Louis Amangin w 1909 nadając jej nazwę `` Eurotium amstelodami '' .
Anamorfę odkryli Charles Thom i Margaret Church w 1926 r. Zaliczyli ją do rodzaju `` Aspergillus '' .
Synonimy :
Charakterystyka .
Gatunek ten występuje głównie w krajach o klimacie tropikalnym i subtropikalnym , m.in .
w przechowalniach i na wysuszonych częściach roślin .
W Polsce notowany był w glebie oraz na nasionach i innych częściach słonecznika rocznego , pomidora zwyczajnego i pszenicy .
U ludzi i zwierząt może wywoływać aspergilozę .
