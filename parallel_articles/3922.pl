Kropidlak zielony
Kropidlak zielony ( `` Aspergillus glaucus '' ( L. ) Link ) – gatunek grzybów z rodziny kropidlakowatych ( `` Aspergillaceae '' ) .
Mikroskopijny grzyb strzępkowy , łagodnie chorobotwórczy , ale mający wiele przydatnych potencjalnych zastosowań w medycynie i produkcji artykułów spożywczych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Aspergillus , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten po raz pierwszy opisał Karol Linneusz w 1753 r. nadając mu nazwę `` Mucor glaucus '' .
Obecną , akceptowaną przez Index Fungorum nazwę nadał mu Johann Heinrich Friedrich Link w 1809 r .
