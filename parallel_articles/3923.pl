Euzodiomyces lathrobii
Euzodiomyces lathrobii Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Euzodiomyces , Euceratomycetaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1900 r. Roland Thaxter na owadach `` Lathrobium punctatum '' , `` Lathrobium multipunctatum '' i `` Lathrobium filiforme '' w Europie .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski w 1994 i 2003 r. opisał jego występowanie na chrząszczach z rodziny kusakowatych ( Staphylinidae ) : `` Lathrobium brunnipes '' , `` Lathrobium elongatum '' , `` Lathrobium filiforme '' , `` Lathrobium fovulum '' , `` Lathrobium fulvipenne '' , `` Lathrobium longulum '' , `` Lathrobium terminatum '' , `` Lathrobium volgense '' .
