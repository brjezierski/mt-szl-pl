Everett ( Pensylwania )
Everett – osada w hrabstwie Bedford w stanie Pensylwania w Stanach Zjednoczonych .
Zgodnie ze spisem ludności przeprowadzonym w 2000 roku liczy 1906 mieszkańców .
Początkowo osada nosiła nazwę Bloody Run , tak jak potok , nad którym osadnicy stoczyli bitwę z Indianami .
Miasteczko zmieniło nazwę na część polityka i mówcy , Edwarda Everetta .
