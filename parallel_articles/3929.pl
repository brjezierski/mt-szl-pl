Ewa Demarczyk
Ewa Maria Demarczyk ( ur .
16 stycznia 1941 w Krakowie , zm .
14 sierpnia 2020 tamże albo w Wieliczce ) – polska piosenkarka , wykonująca utwory z nurtu poezji śpiewanej , w latach 1962–1972 była związana z kabaretem „ Piwnica pod Baranami ” .
Była uznawana za jedną z najbardziej utalentowanych i charyzmatycznych postaci polskiej sceny muzycznej .
Ceniona była za ekspresję , niebywałą osobowość estradową oraz wybitne zdolności interpretacyjne .
Wielokrotnie nagradzana za swoje wykonania .
Występowała z powodzeniem na całym świecie , m.in .
w Austrii , Niemczech , Francji , Szwecji , Wielkiej Brytanii , USA , Kubie , Brazylii , a gościły ją tak prestiżowe estrady jak Olympia czy Carnegie Hall .
