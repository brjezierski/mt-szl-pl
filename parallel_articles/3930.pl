Ewa Kopacz
z domu Lis ( ur .
3 grudnia 1956 w Skaryszewie ) – polska polityk , pediatra i działaczka samorządowa .
W latach 2011–2014 marszałek Sejmu VII kadencji , w latach 2014–2015 prezes Rady Ministrów , od 2019 wiceprzewodnicząca Parlamentu Europejskiego .
Posłanka na Sejm IV , V , VI , VII i VIII kadencji ( 2001–2019 ) , deputowana do Parlamentu Europejskiego IX kadencji ( od 2019 ) , w latach 2007–2011 minister zdrowia , w latach 2010–2014 , 2016–2021 i od 2021 wiceprzewodnicząca Platformy Obywatelskiej ( w latach 2013–2014 i 2016–2017 I wiceprzewodnicząca ) , w latach 2014–2016 pełniąca obowiązki przewodniczącego PO , w 2019 wiceprzewodnicząca frakcji Europejskiej Partii Ludowej w PE .
Życiorys .
Wykształcenie i praca zawodowa .
Jest córką Mieczysława i Krystyny .
Ojciec był zatrudniony jako mechanik , matka pracowała jako krawcowa .
Wychowywała się w Radomiu , gdzie ukończyła II Liceum Ogólnokształcące im .
Marii Konopnickiej .
W 1981 ukończyła studia lekarskie na Wydziale Lekarskim Akademii Medycznej w Lublinie .
Uzyskała specjalizację pierwszego stopnia w zakresie pediatrii i drugiego stopnia w zakresie medycyny rodzinnej .
Pracowała w pogotowiu i przychodniach w Orońsku i Chlewiskach , a następnie w Szydłowcu , gdzie do 2001 kierowała miejscowym zakładem opieki zdrowotnej .
Działalność polityczna do 2014 .
W latach 80. należała do Zjednoczonego Stronnictwa Ludowego , była prezesem koła ZSL działającego przy ośrodku zdrowia w Orońsku .
W latach 90. dołączyła do Unii Wolności , przewodniczyła strukturom tej partii w województwie radomskim .
W wyborach samorządowych w 1998 uzyskała mandat radnej sejmiku mazowieckiego I kadencji .
