Ewenkowie
Ewenkowie ( dawna nazwa Tunguzi ) – myśliwsko-zbierackie ludy , z grupy tunguskiej , zamieszkujące południowo-środkową Syberię ( Rosja ) .
Nazwa .
Nazwa „ Tunguz ” pochodzi od tatarskiego słowa „ świnia ” , oznaczającego też jedzącego wieprzowinę .
Tatarzy wyznający islam nazywali tak plemiona innych religii .
Sami Ewenkowie nazywają siebie `` Owen '' , `` Ewen '' , `` Ewenk '' .
Niegdyś dawna nazwa Ewenków – Tunguzi rozciągała się również na niektóre ludy tungusko–mandżurskiej grupy językowej , m.in .
: Ewenów , Oroczonów , Nanajów , Oroków i Ugedejczyków .
