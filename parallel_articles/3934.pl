Eweni
Eweni ( , dawniej Lamuci ) – autochtoniczny etnos syberyjski , spokrewniony z Ewenkami i wraz z nimi określany dawniej jako Tunguzi .
Eweni zamieszkują głównie obwód magadański i Kraj Kamczacki , Czukocję , wybrzeże Morza Ochockiego oraz północne rejony Jakucji .
Według danych z roku 2010 w Rosji żyje 22 383 Ewenów .
Ich językiem ojczystym jest język eweński , należący do grupy tunguskiej ( północnej ) języków tungusko-mandżurskich .
Język ten jest obecnie dość szybko wypierany przez język rosyjski i jedynie niespełna połowa Ewenów zna rodzimą mowę .
Tradycyjnymi zajęciami Ewenów było na poły koczownicze myślistwo , rybołówstwo i hodowla reniferów .
Do XIX wieku Eweni wyznawali szamanizm , następnie zostali schrystianizowani w obrządku prawosławnym przez Rosjan , zachowali jednak do dziś liczne przejawy dawnej wiary .
