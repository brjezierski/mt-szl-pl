Język eweński
Język eweński ( lamucki ) – język z grupy tungusko-mandżurskiej , którym posługuje się tunguski lud Ewenów , zamieszkujących rosyjską Syberię .
Blisko spokrewniony z językiem ewenkijskim .
Język ten jest dość szybko wypierany przez język rosyjski ; obecnie jedynie ok. 5,7 tys .
spośród 22 tysięcy Ewenów posługuje się rodzimym językiem .
Do zapisu języka eweńskiego używana jest cyrylica .
