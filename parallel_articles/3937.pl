Kisielnica kędzierzawa
Kisielnica kędzierzawa ( `` Exidia nigricans '' Donk . )
– gatunek grzybów z rodziny uszakowatych ( `` Auriculariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Auriculariaceae , Auriculariales , Auriculariomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' według Index Fungorum .
Synonimy naukowe :
Nazwę polską podał Władysław Wojewoda w 1998 r. ( dla synonimu `` Exidia plana '' ) .
W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako kisielec brodawkowany , kisielec przypłaszczony , wypotek gruczołkowaty , wypotek gruczołkowy , kisielec kędzierzawy .
Badania molekularne wykazały , wielkie podobieństwo między `` Exidia nigricans '' i `` Exidia glandulosa '' .
Morfologia .
Za młodu szary , kulisty lub miseczkowaty , później mózgowato-pofałdowany ; górna powierzchnia delikatnie punktowana , ciemna , szarobrązowa do czarnej .
W stanie wilgotnym jest miękki i wewnątrz przejrzysto galaretowaty , wyschnięty staje się twardy , skorupiasty .
