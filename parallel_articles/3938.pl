Łojówka różowawa
Łojówka różowawa ( `` Exidiopsis effusa '' Bref . )
– gatunek grzybów z rodziny uszakowatych ( `` Auriculariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Exidiopsis , Auriculariaceae , Auriculariales , Auriculariomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy naukowe .
Polską nazwę nadał Władysław Wojewoda w 1977 r ..
Morfologia .
Rozpostarty , ściśle przylegający do podłoża , skórzasty , cienki .
Powierzchnia woskowata i nieco błyszcząca .
Morfologicznie jest bardzo zmienny .
Jego rozmiar jest bardzo różny – od bardzo małego do bardzo dużego .
