Bayern Monachium ( szachy )
Bayern Monachium – klub szachowy założony w 1980 , sekcja Bayernu Monachium .
Dziewięciokrotny mistrz Niemiec .
Historia .
Klub jest następcą powstałego w 1908 roku SC Anderssen Bavaria Monachium i powstał w 1980 roku poprzez połączenie z Bavarią .
Od początku powstania Bayern występował w Bundeslidze .
W latach 80. i 90. klub dziewięciokrotnie zdobył mistrzostwo kraju ( 1983 , 1985 , 1986 , 1989 , 1990 , 1991 , 1992 , 1993 , 1995 ) .
Największym sukcesem klubu jest zdobycie Pucharu Europy w 1992 roku .
Zawodnikami Bayernu byli wówczas m.in .
Robert Hübner , Artur Jusupow i Zoltán Ribli .
W 1995 roku drużyna wycofała się z Bundesligi , po czym występowała w Oberlidze oraz 2 .
Bundeslidze .
W 2008 roku Bayern wrócił do Bundesligi .
Kadra w sezonie 2019/2020 .
