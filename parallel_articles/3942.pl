FC Schalke 04
Fußballclub Gelsenkirchen-Schalke 04 e.V .
( w skrócie FC Schalke 04 ) – niemiecki klub sportowy z siedzibą w Gelsenkirchen .
Został założony 4 maja 1904 .
Drużyna piłkarzy występuje w Bundeslidze .
W historii zapisała się jako siedmiokrotny mistrz Niemiec i czterokrotny zdobywca Pucharu Niemiec .
Schalke 04 jest również utytułowane na arenie europejskiej .
Jest zdobywcą Pucharu UEFA z 1997 , gdy w finałowym dwumeczu pokonało Inter Mediolan .
Barwami klubu są kolory niebieski i biały .
Swoje domowe spotkania drużyna piłkarska rozgrywa na obiekcie Veltins-Arena , mogącym pomieścić ponad 61 tysięcy widzów .
Funkcję trenera pełni Dimitrios Grammozis .
W sezonie 2007/2008 klub zajął 3. miejsce w lidze , rok później 8 , zaś w sezonach 2009/2010 i 2017/2018 został wicemistrzem Niemiec .
Nosi przydomki „ Die Königsblauen ” i „ Die Knappen ” .
Do końca lat 30. także znany jako „ Pollackenklub ” .
Klub posiada także między innymi sekcje piłki nożnej kobiet , koszykówki , piłki ręcznej , lekkoatletyki , tenisa stołowego oraz e-sportu .
W 2021 roku po wyjątkowo słabym sezonie ( 16 punktów w całym sezonie ) klub spadł do 2 .
Bundesligi .
Zdarzyło się to pierwszy raz od blisko 30 lat .
Historia .
1904–1912 : Założenie klubu jako „ Westfalia Schalke ” .
Kilku 14- i 15-letnich mieszkańców górniczej dzielnicy Schalke w mieście Gelsenkirchen pasjonowało się piłką nożną i pewnego dnia postanowili oni założyć klub piłkarski .
