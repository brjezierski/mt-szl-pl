FEET
FEET – śląski zespół folkowy , założony przez Piotra Fesztera pochodzącego z Radzionkowa i Jana Etgensa pochodzącego z Chorzowa .
Nazwa grupy jest akronimem powstałym w wyniku złożenia dwóch pierwszych liter nazwisk założycieli .
FEET wykonuje muzykę typową dla kultury górnośląskiej , teksty utworów śpiewane są po śląsku .
Zespół intensywnie koncertował , głównie na górnośląskich imprezach kulturalnych , m.in .
na imprezach organizowanych przez Radio Piekary , a także w Niemczech .
Ma w dorobku trzy płyty .
Najbardziej znanym przebojem grupy FEET jest piosenka „ Wodzionka ” , która doczekała się ponad 2 mln 609 tys .
odsłon w serwisie Youtube ( stan na 09.02.2022 ) .
Grupa została rozwiązana w lipcu 2008 , powód nie jest znany , przypuszczalnie był nim konflikt pomiędzy członkami .
