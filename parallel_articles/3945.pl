FIFA 100
FIFA 100 – popularna nazwa listy najlepszych żyjących piłkarzy , przygotowanej na jubileusz stulecia Międzynarodowej Federacji Związków Piłkarskich ( FIFA ) .
Lista została ogłoszona w 2004 .
O jej stworzenie władze FIFA poprosiły znakomitego niegdyś piłkarza , Brazylijczyka Pelé .
Ostatecznie Pelé umieścił na niej ponad stu graczy , ale i tak spotkał się z krytycznymi uwagami .
Zarzucano mu choćby pominięcie kilku partnerów z reprezentacji Brazylii .
Osiemnastu cenionych fotografów wykonało artystyczne portrety piłkarzy , które można było obejrzeć na otwartej 4 marca 2004 w Londynie wystawie .
Później ekspozycję można było zwiedzić w wielu miastach świata .
