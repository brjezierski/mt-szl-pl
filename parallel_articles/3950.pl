Rejowiec Fabryczny
Rejowiec Fabryczny ( 1954-57 `` gromada Morawinek '' ) – miasto w województwie lubelskim , w powiecie chełmskim , siedziba gminy wiejskiej Rejowiec Fabryczny .
W latach 1975–1998 miasto administracyjnie należało do województwa chełmskiego .
Istnieje rzymskokatolicka parafia Podwyższenia Krzyża Świętego .
Ma powierzchnię 14 km² .
Według danych GUS z 31 grudnia 2019 r. Rejowiec Fabryczny liczył 4386 mieszkańców .
Rejowiec Fabryczny powstał w 1958 r. z obszaru gromady Morawinek , otrzymując status osiedla .
Prawa miejskie otrzymał w 1962 r ..
Miasto leży w obrębie Pagórów Chełmskich , na terenie stanowiącym w XVIII w. ziemię chełmską .
