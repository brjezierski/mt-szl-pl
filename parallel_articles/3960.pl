Święto
Święto – ogólne określenie szczególnych dni lub dłuższych okresów powtarzających się regularnie .
Święto .
Święto może mieć charakter religijny ( np .
Szczodre Gody ) , narodowy ( np .
Święto Niepodległości ) , państwowy ( np .
Dzień Solidarności i Wolności ) lub międzynarodowy ( np .
Dzień Praw Człowieka ) .
Może upamiętniać jakieś wydarzenie ( np .
bitwę ) , podkreślać wagę pewnych problemów ( np .
Sprzątanie Świata ) , może też być okazją do uczczenia osoby ( np .
Jana Pawła II ) czy grupy społecznej ( np .
pracowników ) lub zawodowej ( np .
górników ) .
Z większością świąt wiążą się pewne zwyczaje i uroczysty sposób celebrowania .
Niektóre święta są też dniem wolnym od pracy .
