Faktysa
Faktysa – zmiękczacz pochodzenia roślinnego , stosowany w produkcji gumy .
Powstaje w wyniku reakcji nienasyconych kwasów tłuszczowych z chlorkiem siarki na zimno ( faktysa jasna ) lub z siarką w temp .
do 160 stopni Celsjusza przy przedmuchiwaniu powietrza { faktysa brunatna } .
Stosuje się ją m.in .
do wyrobu gumek ściernych .
