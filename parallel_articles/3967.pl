Fallausan
Fallausan ( arab .
فلاوسن ; fr .
`` Fellaoucene '' ) – jedna z 53 gmin w prowincji Tilimsan , w Algierii , znajdująca się w północno-zachodniej części prowincji , około 32 km na północny zachód od Tilimsan .
W 2008 roku gminę zamieszkiwało 8781 osób .
Numer statystyczny gminy w Office National des Statistiques d'Algérie to 1320 .
