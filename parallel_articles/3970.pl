20 października 1933 gminę Wilanów podzielono na 27 gromad : Dawidy , Dawidy Bankowe , Dawidy Poduchowne , Falenty Duże , Falenty Nowe , Grabów , Grabówek , Imielin , Janki , Jaworowa , Jeziorki Nowe , Jeziorki Polskie , Krasnowola , Laszczki , Łady , Łazy , Podolszyn , Podolszyn Nowy , Puchały , Raszyn , Rybie , Sękocin Nowy , Sękocin Stary , Słomin , Wyczółki , Wypędy i Zgorzała .
15 października 1937 w gminie Falenty utworzono gromadę Wilcza Góra .
2 września 1938 z gminy Falenty wyłączono część gruntów gromad Grabów i Imielin , włączając je do gminy Wilanów w tymże powiecie i województwie .
