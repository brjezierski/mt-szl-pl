Famagusta
Famagusta ( gr .
`` Αμμόχωστος '' , tur .
`` Gazimağusa '' lub `` Mağusa '' ) – miasto w Tureckiej Republice Cypru Północnego oraz Republice Cypryjskiej .
Stolica północnocypryjskiego dystryktu Famagusta oraz formalna stolica cypryjskiego dystryktu Famagusta .
Położona jest we wschodniej części Cypru , na nizinie Mesaria , nad zatoką Famagusta ( Morze Śródziemne ) .
Zamieszkuje ją 40 920 osób ( 2011 ) .
Historia .
Została założona w III w. p.n.e .
