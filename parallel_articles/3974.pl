Flaga Belgii
Flaga Belgii – jeden z symboli państwowych Królestwa Belgii .
Historia i symbolika .
Wzór flagi przejęto z francuskiej `` Tricolore '' .
Barwy pochodzą z herbu księstwa Brabancji , znanego od XII wieku .
Wojna o niepodległość Belgii zaczęła się w Brabancji i tam znalazła najwięcej zwolenników , dlatego herb ten przyjęto jako herb Belgii ( w polu czarnym lew złoty wspięty z czerwonym orężem ) .
Wprowadzona w sierpniu 1830 , przyjęta oficjalnie 23 stycznia 1831 roku .
Konstrukcja i wymiary .
Prostokąt o proporcjach 13:15 podzielony na trzy równe pionowe pasy : czarny , żółty , czerwony .
