Flaga Danii
Flaga Danii lub Dannebrog – jeden z symboli państwowych Królestwa Danii .
Wygląd .
Flaga Danii to czerwony prostokąt z białym krzyżem skandynawskim .
Historia .
Jest ona jedną z najstarszych nieprzerwanie używanych flag suwerennych państw na świecie .
Jej początki datuje się na pierwszą połowę XIII wieku .
Istnieje wiele teorii na temat jej pochodzenia .
Najbardziej znana wiąże ją z bitwą pod Lyndanisse ( niedaleko dzisiejszego Tallinna ) , która miała miejsce 15 czerwca 1219 w czasie krucjaty liwońskiej duńskiego króla Waldemara II do Estonii .
Wówczas miała być użyta po raz pierwszy .
Według legendy flaga miała ukazać się na niebie Waldemarowi II , a w krytycznym momencie spaść z nieba w ręce modlącego się arcybiskupa duńskiego .
Inna teoria głosi , że użyto ją dopiero na pogrzebie Waldemara II w 1241 roku .
Pierwsze oficjalne wzmianki o `` Dannebrog '' pochodzą z XIV wieku , jednak dowody wskazują na to , że Dania nie była jedynym państwem używającego takiego wzoru .
Wiele terytoriów wchodzących w skład I Rzeszy ( np .
Sabaudia lub Szwajcaria ) posiadało podobne symbole .
Flaga zawierająca biały krzyż na czerwonym tle była także flagą wojenną samego Cesarstwa .
Historycy łączą ją również z chorągwiami krzyżowców .
Takiej samej bandery używali Zakon Maltański ( joannici ) oraz wyruszający na krucjaty niemieccy rycerze .
Jest jednym z wariantów często używanej w średniowieczu chorągwi z krzyżem św .
Jerzego i w takiej samej kolorystyce pojawiła się po obu stronach w bitwie pod Grunwaldem .
Przesunięcie krzyża było wówczas modne , o czym świadczą również chorągwie krzyżackie ( zob .
`` Banderia Prutenorum '' ) .
Najstarszy znany kolorowy wizerunek `` Dannebrog '' znajduje się w `` Herbarzu Geldrii '' datowanym na ok. 1340–1370 , gdzie ma kształt kwadratowy .
Można ją też dostrzec , z centralnym krzyżem , na pieczęci Eryka Pomorskiego jako króla unii kalmarskiej z 1398 r. Przesunięty krzyż nie był regułą .
Jeszcze do XVIII wieku używano krzyża centralnego , często na fladze kwadratowej .
Przez cały czas `` Dannebrog '' był używany w najróżniejszych wariantach , czasem z krzyżem skandynawskim , czasem z krzyżem pośrodku ( jak w fladze Szwajcarii ) , czasem na prostokątnej , a czasem kwadratowej fladze .
Oficjalnie po raz pierwszy `` Dannebrog '' wyznaczono jako symbol duńskiej marynarki wojennej w 1625 roku ( w kształcie jaskółczego ogona ) , w 1748 roku dla statków handlowych ( najprawdopodobniej prostokątny ) , a w 1854 roku stał się oficjalnie flagą narodową Danii ( wersja z krzyżem skandynawskim ) .
W XIX wieku duński sztandar , nazywany `` Dannebrog '' ( z fryzyjskiego `` dan '' = `` czerwony '' + z dolnoniemieckiego `` brog '' = `` tkanina , płótno '' ) był popularny również w innych krajach Skandynawii i stał się wzorem dla flag : Finlandii , Islandii , Norwegii i Szwecji , a także Wysp Owczych , Wysp Alandzkich i Szetlandów ( zob .
krzyż skandynawski ) .
Flaga posiada różne wersje , a ich używanie jest ściśle regulowane prawem .
Duńczycy w sposób szczególny przywiązani są do swego symbolu , który towarzyszy im w czasie różnych świąt i uroczystości prywatnych ( np .
Boże Narodzenie , urodziny ) .
Konstrukcja i wymiary .
1 maja 1893 zostały ustalone maksymalne proporcje flagi jako 28:37 , ale nie odwołano ani nie zastąpiono proporcji 28:34 ustalonych 11 lipca 1748 .
Proporcje flagi Danii wynoszą więc 12:4:18-21 w poziomie i 12:4:12 w pionie .
