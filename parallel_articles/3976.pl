Flaga Francji
Flaga Francji – jeden z symboli państwowych Republiki Francuskiej , często nazywana `` Tricolore '' ( ) lub `` Le drapeau tricolore '' ( ) .
Flaga jest również symbolem wszystkich francuskich terytoriów zależnych .
Wygląd i symbolika .
Wygląd .
Flaga Republiki Francuskiej jest prostokątem o proporcjach 2:3 , podzielonym na trzy równe , pionowe pasy : niebieski , biały i czerwony .
Symbolika .
Flaga , podobnie jak większość wczesnych flag państwowych , nie ma oficjalnej symboliki .
Kolor biały najprawdopodobniej nawiązuje do wcześniejszej białej flagi królewskiej Burbonów , a kolory czerwony i niebieski to tradycyjne kolory Paryża .
Barwy te są oficjalnie wiązane z ideałami rewolucji francuskiej : wolnością , równością i braterstwem .
Historia .
Podczas ancien régime ' u Francja używała wielu flag , bazowanych na herbie , czyli niebieskiej tarczy z trzema złotymi fleur-de-lis .
