Flaga Gliwic
Flaga Gliwic – jeden z symboli miasta Gliwice w postaci flagi ustanowiony uchwałą rady miejskiej nr XXIX/392/96 z 14 listopada 1996 .
Wygląd i symbolika .
Flaga Gliwic składa się z dwóch poziomych pasów .
Pas górny jest koloru niebieskiego , a dolny koloru czerwonego .
Na środku flagi może znajdować się herb miasta .
