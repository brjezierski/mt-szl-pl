Flaga Łazisk Górnych
Flaga Łazisk Górnych – jeden z symboli miasta Łaziska Górne w postaci flagi ustanowiony uchwałą rady miejskiej nr XIII/121/11 z 25 października 2011 .
Wygląd i symbolika .
Flaga obowiązująca od 5 stycznia 2012 jest żółta z dwoma poziomymi pasami koloru błękitnego ( każdy szerokości dziesiątej części szerokości flagi ) znajdującymi się na zewnętrznej , górnej i dolnej , części flagi .
W środku , w żółtym pasie , umieszczono herb miasta .
Flaga ma wymiary 5:8 , została przyjęta uchwałą Rady Miejskiej 25 października 2011 r .
Historia .
Do 4 stycznia 2012 flaga przedstawiała herb miasta umieszczony na białym tle .
W przeszłości nad tarczą herbową znajdował się napis `` ŁAZISKA GÓRNE '' ( podobnie jak na starej wersji herbu ) , ale został usunięty jako niezgodny z zasadami heraldyki .
Zasady używania flagi zostały określone uchwałą rady miejskiej nr XXXVII/327/05 z dnia 18 października 2005 roku , uchylone w uchwale z 25 października 2011 .
