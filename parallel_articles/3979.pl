Flaga Górnego Śląska
Flaga Górnego Śląska to dwubarwna flaga w kolorach złotym i niebieskim .
Flaga składa się z dwóch poziomych pasów .
Barwy flagi nawiązują do herbu Górnego Śląska , co oznacza , że zgodnie z zasadami heraldyki kolor złoty orła znajduje się u góry , a kolor niebieski tła u dołu .
Województwo śląskie również używa koloru złotego i niebieskiego .
Flagą rządową województwa śląskiego jest herb z orłem na błękitnym polu .
Podobnymi flagami , które również zawierają barwy złoto-niebieskie , są flagi miast Opole i Katowice .
Jest to popularny symbol Ruchu Autonomii Śląska .
15 lipca z inicjatywy Marka Plury jest obchodzony Dzień Śląskiej Flagi .
