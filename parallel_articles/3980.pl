Flaga Włoch
Flaga Włoch – jeden z symboli państwowych Republiki Włoskiej , który jest w kształcie prostokąta podzielonego na trzy pionowe pasy w barwach : zielona , biała , czerwona .
Symbolika .
Trójkolorowe pasy nawiązują do rewolucji francuskiej , podkreślając znaczenie `` trzeciego stanu '' w rządzeniu państwem .
Potocznie uważa się także , że kolory symbolizują : zielony – wolność , nadzieja i krajobraz śródziemnomorski , biały – wiara katolicka i ośnieżone szczyty Alp , czerwony – męstwo i krew tych , którzy walczyli o zjednoczenie Włoch .
Taki sam zestaw kolorów mają flagi państwowe : Meksyku , Bułgarii , Węgier , Iranu .
Historia .
Po raz pierwszy trójkolorowa flaga pojawiła się w 1795 roku w Bolonii podczas zamieszek studenckich .
Oficjalnie flagą włoską została w 1796 roku dzięki Napoleonowi , który ją przyjął dla Legionu Lombardzkiego .
Używano jej od 11 maja 1798 do 20 sierpnia 1802 .
Towarzyszyła wszystkim walkom o niepodległość , łącząc się z ideami jedności i niepodległości narodowej .
W 1848 król Sardynii przywrócił ją i w centrum umieścił herb swej dynastii sabaudzkiej .
Pod koniec II wojny światowej trójkolorowa flaga bez symboli była uznawana za flagę Włoskiej Republiki Socjalnej .
W obecnym kształcie została zatwierdzona w konstytucji republiki 19 czerwca 1946 .
Konstrukcja i wymiary .
Prostokąt o proporcjach 2:3 podzielony na trzy równe pionowe pasy : zielony , biały , czerwony .
