Flaga Katowic
Flaga Katowic – jeden z symboli miasta Katowice w postaci flagi , ustanowiony uchwałą rady miejskiej 10 kwietnia 1996 .
Wygląd i symbolika .
Flaga została zaprojektowana jako prostokątny płat z dwoma równymi pasami , z których górny jest w kolorze żółtym , a dolny w kolorze błękitnym .
Pośrodku flagi umieszczony jest herb Katowic .
