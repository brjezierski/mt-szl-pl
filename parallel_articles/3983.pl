Flaga Polski
Flaga Polski ( Flaga Państwowa Rzeczypospolitej Polskiej ) – jeden z symboli państwowych Rzeczypospolitej Polskiej .
Według ustawy z dnia 31 stycznia 1980 r. o godle , barwach i hymnie Rzeczypospolitej Polskiej oraz o pieczęciach państwowych , jest nią prostokątny płat tkaniny o barwach Rzeczypospolitej Polskiej i proporcji 5:8 , umieszczony na maszcie .
Zgodnie z art .
6 ust .
2 ustawy , za flagę Polski uważany jest także wariant z godłem Polski , umieszczonym pośrodku białego pasa .
Barwy Rzeczypospolitej Polskiej stanowią składniki flagi państwowej Rzeczypospolitej Polskiej .
Ustawa stanowi , że barwami Rzeczypospolitej Polskiej są kolory biały i czerwony , ułożone w dwóch poziomych , równoległych pasach tej samej szerokości , z których górny jest koloru białego , a dolny koloru czerwonego .
Od 2004 roku 2 maja jest w Polsce oficjalnie obchodzony jako Dzień Flagi Rzeczypospolitej Polskiej .
Znaczenie heraldyczne barw .
Barwy flagi złożone z dwóch poziomych pasów : białego oraz czerwonego są odwzorowaniem kolorystyki godła państwowego , który stanowi orzeł biały na czerwonym polu .
Zgodnie z zasadami heraldyki pas górny reprezentuje białego orła , a dolny czerwone pole tarczy herbowej .
