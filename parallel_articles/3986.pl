Flaga Skopja
Flaga Skopja – flaga stolicy Macedonii Północnej , Skopja stanowi płat tkaniny o proporcjach 2:1 koloru czerwonego zawierający w lewym górnym rogu złoty kontur herbu miasta tj .
Kamienny Most na rzece Wardar , twierdzę Skopsko Kale i góry ( Skopska Crna Gora ) w tle .
Kształt flagi reguluje statut miasta .
