Flaga Sycylii
Flaga Sycylii została przyjęta po raz pierwszy w 1282 roku , po powstaniu zwanym nieszporami sycylijskimi w Palermo .
Przedstawia centralnie umieszczony triskelion , pośrodku którego widnieje głowa Meduzy , otoczona trzema kłosami pszenicy .
Trzy nogi są tutaj symbolem szczęścia i pomyślności .
Flaga Sycylii podzielona jest na dwa równej wielkości trójkąty wzdłuż przekątnej biegnącej od górnego końca po stronie przy drzewcu do dolnego po stronie swobodnej .
Górny trójkąt jest barwy czerwonej , a dolny żółtej .
Są to barwy dwóch sycylijskich miast , które jako pierwsze zawiązały konfederację by przeciwstawić się władzy Andegawenów : Palermo i Corleone .
Triskelion , zwany na Sycylii `` trinacria '' , jest oficjalnym symbolem Sycylii .
Flaga w obecnej formie została przyjęta w roku 2000 jako flaga regionu autonomicznego Sycylia .
