Flaga Wrocławia
Flaga Wrocławia – jeden z symboli miejskich Wrocławia .
Symbolika .
Do 1938 barwami Wrocławia były kolory czerwony i biały umieszczone na fladze w formie poziomych pasów – czterech ( 2 czerwone + 2 białe ) lub pięciu ( 3 czerwone + 2 białe ) .
Widnieją one w klejnocie wielkiego herbu miasta.Przed 1938 używana także była flaga miejska z herbem .
Obecnie jako barwy herbowe Wrocławia funkcjonują kolory czerwony i żółty .
Na sztandarach pionowych pas czerwony znajduje się z lewej strony , pas żółty z prawej .
W przypadku sztandarów poziomych górną połowę stanowi pas czerwony , a dolną pas żółty , a proporcje wysokości do szerokości flagi wynoszą 5:8 .
Dopuszczalne jest umieszczanie herbu miasta na tle flagi .
