Flaga
Flaga – płat tkaniny określonego kształtu i barwy ( barw ) przymocowywany do drzewca ; może zawierać godła , symbole , wizerunki .
Flaga jest znakiem rozpoznawczym , symbolem państwa ( flaga państwowa ) lub organizacji , czasem również miast ( flaga miejska ) czy jednostek podziału administracyjnego , oddziału wojsk ( sztandar ) , organizacji : politycznych , społecznych , kościelnych , sportowych itd .
lub jednostki .
