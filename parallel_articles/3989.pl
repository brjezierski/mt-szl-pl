biała flaga parlamentariusza i kapitulacji ) i kodowych ( np .
flagi sygnalizujące dostępność kąpieliska , flagi wyścigowe w sportach samochodowych , flagi Międzynarodowego Kodu Sygnałowego w żegludze ) .
