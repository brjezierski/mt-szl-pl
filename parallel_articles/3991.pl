Flaga Śląska
Flaga Śląska i Dolnego Śląska to dwubarwna flaga w kolorach srebrnym i złotym .
Flaga składa się z dwóch poziomych pasów .
Barwy flagi nawiązują do herbu Śląska , dlatego zgodnie z zasadami heraldyki , kolor tła złoty znajduje się na dole .
Tradycyjnie flaga Śląska jest jednocześnie flagą Dolnego Śląska , natomiast Górny Śląsk posiada własną flagę .
Barwy te były używane razem z herbem prowincji śląskiej oraz z herbem prowincji dolnośląskiej .
W Saksonii .
Art .
2 ust .
4 Konstytucji Wolnego Państwa Saksonii umożliwia używanie na śląskim obszarze Saksonii flagi Dolnego Śląska na równi z flagą Saksonii .
Kraj morawsko-śląski .
Kraj Morawsko-Śląski w Czechach ma własną flagę .
Na pierwszej ćwiartce widnieje orzeł ( dolnośląski ) , który pojawia się także w innych śląskich herbach oraz w herbie Republiki Czeskiej .
W Polsce .
Flaga województwa dolnośląskiego przedstawia orła śląskiego na złotym tle .
W tradycji polskiej orzeł śląski nie jest ukoronowany ( z wyjątkiem Śląska Cieszyńskiego ) , w czeskiej i niemieckiej na ogół odwrotnie .
