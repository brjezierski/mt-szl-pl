Farad ( F ) – jednostka pojemności elektrycznej w układzie SI ( jednostka pochodna układu SI ) .
Przewodnik elektryczny ma pojemność elektryczną jednego farada , gdy zwiększa potencjał o 1 wolt po dostarczeniu ładunku 1 kulomba .
W uproszczeniu pojemność jednego farada oznacza , że w przewodniku o potencjale jednego wolta można „ umieścić ” ładunek o wartości jednego kulomba .
Jest to zarazem pojemność kondensatora , w którym naładowanie okładek przeciwnymi ładunkami o wartości 1 kulomba wytwarza napięcie 1 wolta .
