Historia .
Pojęcie „ farad ” zostało początkowo wprowadzone przez Josiaha Latimera Clarka i Charlesa Brighta w roku 1861 jako jednostka ładunku .
W roku 1881 , na międzynarodowym kongresie elektryków w Paryżu , termin ten był już oficjalnie używany jako określenie pojemności elektrycznej .
