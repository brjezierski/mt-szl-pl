Fargo ( film )
Fargo – amerykańska czarna komedia kryminalna neo-noir z 1996 , zrealizowana przez braci Ethana i Joela Coenów .
Za scenariusz `` Fargo '' Coenowie zdobyli Oscara , a Frances McDormand została uhonorowana statuetką dla najlepszej aktorki w roli pierwszoplanowej .
W 2014 roku swoją premierę miał inspirowany filmem serial `` Fargo '' .
Opis fabuły .
Akcja filmu rozgrywa się wśród śniegów Minnesoty ( rodzinnego stanu Coenów ) .
Jerry Lundegaard ( Macy ) , dyrektor sprzedaży w salonie samochodowym swego teścia , wpada w finansowe tarapaty , których szczegóły nie są podane .
Wynajmuje dwóch psychopatycznych bandziorów ( Buscemi i Stormare ) , aby porwali jego żonę – pieniądze na okup zamierza wyłudzić od jej ojca .
Tuż po porwaniu samochód sprawców zostaje zatrzymany do rutynowej kontroli przez samotnego policjanta , jeden z porywaczy go morduje .
Od tego momentu nic nie powstrzyma tragicznego rozwoju wypadków .
Bandytów ściga prowincjonalna policjantka w zaawansowanej ciąży , Marge Gunderson ( McDormand ) .
