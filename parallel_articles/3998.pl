Farhad Vladi
Farhad Vladi ( ur .
1945 ) – niemiecki przedsiębiorca pochodzenia irańskiego .
Jest właścicielem firmy Vladi Private Islands , założonej w 1970 w Hamburgu , będącej światowym liderem w branży handlu wyspami .
Od momentu założenia sprzedał ponad 1800 wysp na Karaibach , w Polinezji i w innych zakątkach świata .
