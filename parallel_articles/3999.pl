Trupia farma ( serial telewizyjny )
Trupia farma ( ang .
`` The Body Farm '' , 2011 ) – brytyjski serial kryminalny wyprodukowany przez Liquid Television .
Światowa premiera serialu miała miejsce 13 września 2011 roku na antenie BBC One .
Ostatni odcinek serialu został wyemitowany 18 października 2011 roku .
W Polsce premiera serialu odbyła się 8 sierpnia 2012 roku na kanale Ale Kino+ .
Opis fabuły .
Patolog Eve Lockhart pracuje w ośrodku badawczym , zwanym „ trupią farmą ” , w którym razem ze swoimi współpracownikami Rosą i Oggym zajmują się obserwacją procesów rozkładu ludzkiego ciała .
