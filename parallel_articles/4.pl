.af
.af – krajowa domena internetowa najwyższego poziomu przypisana dla stron internetowych z Afganistanu , jest aktywna od 1997 roku i administrowana przez afgańskie Ministerstwo Komunikacji .
Aktualnie w bardzo niewielkim użyciu .
Obecnie stawki za rejestrację domeny są 5-krotnie wyższe dla obcokrajowców .
Poza tym domeny drugiego poziomu są 50 razy droższe od domen trzeciego poziomu , sięgając przy tym absurdalnych kwot .
