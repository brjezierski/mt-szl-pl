Stan obecny .
Kościół katolicki .
W Kościele katolickim proboszcz jest mianowanym przez biskupa duszpasterzem i zarządcą parafii .
Jest więc odpowiedzialny za duszpasterstwo parafian i dobra materialne parafii .
Status proboszcza prawo kanoniczne określa sformułowaniem : `` Własny pasterz zleconej sobie parafii pod władzą biskupa '' ( `` Pastor proprius paroeciae sibi commissae sub auctoritate Episcopi '' – kan. 519 ) .
Proboszczem zostać może wyłącznie prezbiter , którego poglądy są zgodne z magisterium Kościoła i który nie budzi zastrzeżeń obyczajowych i moralnych .
Jeżeli prezbiter żyje w małżeństwie , jego żona i dzieci z nim mieszkające również nie mogą budzić zastrzeżeń obyczajowych .
Ponadto musi być wolny od przeszkód prawnych prawa kanonicznego i powszechnego .
Kandydat na proboszcza musi potwierdzić swoją przydatność przez złożenie egzaminu przed własnym biskupem .
Metodę i przedmiot egzaminu określa biskup .
Mianowany na proboszcza powinien swój urząd objąć na czas nieokreślony , ponieważ stałość ma być jednym z jego podstawowych przymiotów .
Istnieje jednak możliwość , by urząd proboszcza obsadzany był kadencyjnie .
Taka decyzja wymaga jednak zgody Konferencji Episkopatu wyrażonej w formie dekretu i potwierdzonej przez Stolicę Apostolską .
W obsadzaniu urzędu proboszcza biskup działa w sposób wolny i niezależny .
Powinien jednak wysłuchać w tej sprawie przede wszystkim opinii dziekana , oraz wszystkich , duchownych i świeckich , którzy mu w tej decyzji mogą służyć radą .
Mianowanego proboszcza w objęty urząd i parafię wprowadza jego ordynariusz w sposób przyjęty zwyczajowo w danej diecezji .
Minimalną formą wprowadzenia , wymaganą prawem kanonicznym jest publiczne przedstawienie parafianom dekretu o nominacji z wyznaczeniem daty jej obowiązywania .
