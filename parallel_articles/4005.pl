Farrah Fawcett
Mary Farrah Leni Fawcett , znana jako Farrah Fawcett , a w latach 1973–1978 także jako Farrah Fawcett-Majors ( ur .
2 lutego 1947 w Corpus Christi w stanie Teksas , zm .
25 czerwca 2009 w Santa Monica w stanie Kalifornia ) – amerykańska aktorka , modelka i producentka telewizyjna .
Stała się symbolem małego ekranu lat 70 .
XX w. , była ulubienicą Ameryki w pierwszym sezonie serialu ABC `` Aniołki Charliego '' .
Była czterokrotnie nominowana do nagrody Emmy ( 1985 , 1990 , 2003 , 2009 ) i sześciokrotnie do Złotego Globu ( 1977 , 1985 , dwie nominacje w 1987 , 1988 , 1990 ) .
Została uhonorowana trzema brązowymi statuetkami Bravo Otto dla najlepszej gwiazdy telewizyjnej ( 1979 , 1980 ) i najlepszej aktorki ( 1981 ) , przyznawanymi przez niemiecki dwutygodnik dla młodzieży „ Bravo ” .
W 1996 program telewizyjny „ TV Guide ” umieścił ją na 26. miejscu listy w rankingu 50 .
Największych Gwiazd TV w dziejach .
Była na okładkach magazynów takich jak „ Cosmopolitan ” , „ Ekran ” , „ Elle ” , „ Film ” , „ Harper ’ s Bazaar ” , „ People ” , „ Playboy ” , „ Time ” , „ TV Guide ” , „ Vanity Fair ” i „ Vogue ” .
W 1995 otrzymała własną gwiazdę w Alei Gwiazd w Los Angeles znajdującą się przy 7051 Hollywood Boulevard .
