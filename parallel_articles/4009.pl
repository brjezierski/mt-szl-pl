Fastów
Fastów ( , `` Fastiw '' ) – miasto na Ukrainie , stolica rejonu w obwodzie kijowskim .
Około 44 tys .
mieszkańców ( 2021 ) .
Do XIX wieku oficjalna nazwa miasta to „ Chwastów ” .
Był miastem duchownym Rzeczypospolitej Obojga Narodów .
Historia .
Wyludnione na skutek tatarskich najazdów tereny w XVI wieku szlachcic Filon Hulkiewicz przekazał biskupowi kijowskiemu Jakubowi Woronieckiemu , a zaczął je kolonizować biskup Józef Wereszczyński , który uczynił osadę siedzibą katolickich biskupów kijowskich , którzy uczynili z Fastowa ośrodek swojej domeny biskupiej .
Biskup Wereszczyński ufundował też tutaj parafię i kościół , a nawet założył drukarnię .
