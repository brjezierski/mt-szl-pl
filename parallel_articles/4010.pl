Fatin Hamama
Fatin Hamama ( arab .
فاتن حمامة , `` Fātin Ḥamāmah '' ; ur .
27 maja 1931 w Al-Mansurze , zm .
17 stycznia 2015 w Kairze ) – egipska aktorka .
Życiorys .
Fatin Hamama urodziła się rodzinie muzułmańskiej .
Jej ojciec Ahmad Hamama pracował jako urzędnik w egipskim Ministerstwie Edukacji , a jej matka była gospodynią domową .
Miała dwóch braci i jedną siostrę .
W 1939 roku , mając 7 lat , zagrała w filmie `` Jaum Sa ’ id '' .
Cztery lata później zagrała w obrazie `` Rasasa fi al-kalb '' .
Jusuf Wahbi zaproponował jej główną rolę w filmie `` Malak ar-Rahma '' .
Film przyciągnął powszechną uwagę mediów potem zagrała wiele innych ról .
Była także producentką filmową .
Laureatka wielu nagród .
Życie osobiste .
Pierwszym jej mężem był reżyser Izz ad-Din Zu al-Fikar , wyszła za niego za mąż w 1947 roku , a rozwiodła się po sześciu latach – z tego związku ma córkę .
W 1955 roku poślubiła Omara Sharifa i w 1957 roku urodziła mu syna , jednak małżeństwo zakończyło się rozwodem w 1974 roku .
Jej ostatnim mężem był Muhammad Abd al-Wahhab .
