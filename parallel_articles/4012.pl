Faulx ( 1911 )
Faulx – francuski niszczyciel z okresu I wojny światowej .
Jednostka typu `` Bouclier '' .
Okręt wyposażony w cztery kotły parowe opalane ropą i turbiny parowe .
W czasie I wojny światowej operował na Morzu Śródziemnym .
18 kwietnia 1918 roku zatonął w Cieśninie Otranto staranowany przez francuski niszczyciel `` Mangini '' .
