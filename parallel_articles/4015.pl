Faya
Faya , Faya-Largeau , Largeau – miasto w Czadzie , stolica regionu Borku ( dawniej regionu Bourkou-Ennedi-Tibesti obejmującego cały północny Czad ) oraz departamentu Borkou ; 13 400 mieszkańców ( 2005 ) .
Pierwotną nazwę `` Faya '' Francuzi przemianowali w czasach kolonialnych ( początek XX w. ) na `` Largeau '' na cześć pułkownika Étienne Largeau , zasłużonego dla władzy francuskiej w regionie .
Po uzyskaniu niepodległości przez Czad pod nazwą `` Faya-Largeau '' .
Miasto znajdowało się pod okupacją libijską w latach 1975-1980 i 1983-1987 .
