Bażant zwyczajny
Bażant zwyczajny , bażant ( `` Phasianus colchicus '' ) – gatunek dużego ptaka z rodziny kurowatych ( Phasianidae ) .
W stanie dzikim występuje w umiarkowanych obszarach Azji i na zachód po skrajnie południowo-wschodnią Europę w 30 podgatunkach .
Introdukowany do wielu regionów na świecie , w tym do Europy Środkowej , do której sprowadzono go z Chin w średniowieczu .
Nie jest zagrożony wyginięciem .
Długość ciała samców 75–89 cm , samic 53–62 cm ; rozpiętość skrzydeł 70–90 cm .
Występuje dymorfizm płciowy .
U ptaków obojga płci występuje długi ogon .
Samiec jest głównie rdzawobrązowy , pokryty ciemnymi plamkami .
