Feldafing
Feldafing – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Monachium , w powiecie Starnberg .
Leży około 5 km na południowy zachód od Starnberga , nad jeziorem Starnberger See , przy linii kolejowej Monachium – Garmisch-Partenkirchen - Innsbruck .
Polityka .
Wójtem gminy jest Bernhard Sontheim z BG , rada gminy składa się z 16 osób .
