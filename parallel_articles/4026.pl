Fellatio ( łac . )
– metoda seksu oralnego polegająca na stymulowaniu narządów płciowych ( penisa i moszny ) mężczyzny za pomocą ust , warg bądź języka .
Nazwa .
Termin `` fellatio '' pochodzi z łacińskiego czasownika `` fellare '' ( ssać ) .
Jest to nazwa używana znacznie rzadziej niż określenie `` seks oralny '' , odnoszące się także do żeńskiego odpowiednika , którym jest `` cunnilingus '' .
Istnieje wiele potocznych i wulgarnych określeń tej formy aktywności seksualnej .
Do najpopularniejszych należą np .
`` robić loda '' , `` robić laskę '' , `` obciągać '' .
