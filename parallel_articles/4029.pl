Ferdynand Magellan
Ferdynand Magellan , , ( ur .
wiosną 1480 , zm .
27 kwietnia 1521 ) – żeglarz portugalski w służbie hiszpańskiej , odkrywca i morski podróżnik .
Jedna z najwybitniejszych postaci w dziejach żeglugi i odkryć geograficznych .
20 września 1519 roku wyruszył z Hiszpanii drogą zachodnią , czyli przez Ocean Atlantycki , do Wysp Korzennych w Archipelagu Malajskim .
Wyprawa , która wypłynęła pod jego dowództwem , jako pierwsza opłynęła Ziemię .
Zginął 27 kwietnia 1521 w walce z mieszkańcami wyspy Mactan w Archipelagu Filipińskim , opierającymi się kolonizacji .
Życiorys .
Ferdynand Magellan urodził się w Ponte da Barca , na terenie współczesnego dystryktu Viana do Castelo , w położonym na północnym zachodzie Portugalii regionie Minho .
Jego rodzicami byli Pedro Rui de Magalhães .
Magellan miał dwoje rodzeństwa : brata Diega de Sousę i siostrę Isabel .
Rodzice Magellana zmarli , gdy miał 10 lat .
W wieku 12 lat Magellan został paziem króla Jana II i królowej Eleonory na dworze królewskim w Lizbonie , dokąd jego brat przyjechał dwa lata wcześniej .
Tutaj , razem z kuzynem Francisco Serrano , Magellan kontynuował edukację , interesując się szczególnie geografią i historią .
Niektórzy spekulują , że jego nauczycielem mógł być Martin Behaim .
W 1495 zmarł jego protektor , król Jan II , a jego następcą został Manuel I , przeciwnik swego poprzednika i jego ludzi .
W wieku 25 lat Ferdynand wypłynął po raz pierwszy na morze .
W 1505 roku popłynął do Indii , aby wprowadzić tam Francisca de Almeidę na urząd portugalskiego wicekróla i założyć po drodze bazy wojskowe i morskie .
To właśnie tam uzyskał chrzest bojowy : gdy miejscowy król odmówił płacenia daniny , oddziały Almeidy zdobyły muzułmańskie miasto Kilwa w dzisiejszej Tanzanii .
W 1513 roku Magellan został wysłany do Maroka , gdzie 28 sierpnia wziął udział w bitwie pod Azamorem i został poważnie ranny w kolano w trakcie oblężenia mauretańsko-marokańskiej twierdzy .
Mimo awansu na kwatermistrza Magellanowi nie wiodło się dobrze : został oskarżony o nielegalny handel z muzułmańskimi Mauretanami .
Wdał się też w konflikt z Almeidą : gdy opuścił bez zezwolenia armię , Almeida posłał negatywny raport o żeglarzu na portugalski dwór .
Kilka oskarżeń wycofano , ale Magellan popadł w niełaskę króla Manuela I , który wprawdzie podniósł go do godności `` fidalgo escudeiro '' , ale odmówił podwyższenia dochodów .
