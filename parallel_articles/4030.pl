Ferenc Karinthy
Ferenc Karinthy ( ur .
2 czerwca 1921 w Budapeszcie , zmarł 29 lutego 1992 w Budapeszcie ) – pisarz i reportażysta węgierski , syn innego pisarza Frigyesa Karinthiego , z wykształcenia lingwista .
Po ukończeniu studiów pracował w budapeszteńskich teatrach jako dramaturg .
Pierwszą jego powieścią była `` Noc Don Juana '' , za którą zdobył niemałe uznanie .
Drugie z kolei , surrealistyczne dzieło – `` Wywoływanie duchów '' poświęcił ojcu oraz prozaikowi Dezső Kosztolányiemu .
W późniejszej twórczości skłaniał się do idei realizmu .
W tej konwencji utrzymany jest m.in .
zbiór opowiadań `` Piękne życie '' , powieści `` Centaur '' i `` Murarze '' .
Ferenc Karinthy był wielokrotnym laureatem nagrody literackiej im .
Attili Józsefa , m.in .
za powieść `` Budapeszteńska wiosna '' ( 1953 ) .
Za `` Reportaże literackie '' otrzymał nagrodę im .
Kossutha .
