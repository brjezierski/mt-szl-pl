Ferenc Puskás
Ferenc Puskás ( wym .
; prawdziwe nazwisko `` Ferenc Purczeld '' ; ur .
1 kwietnia 1927 roku w Budapeszcie , zm .
17 listopada 2006 roku tamże ) – węgierski piłkarz i trener piłkarski , zwany przez rodaków `` Puskás Öcsi '' ( `` öcsi '' tłumaczy się jako „ młodszy brat ” ) .
Wicemistrz świata 1954 i złoty medalista Igrzysk Olimpijskich 1952 .
Jeden z najwybitniejszych piłkarzy w historii piłki nożnej .
Zmarł 17 listopada 2006 roku po długiej i ciężkiej chorobie , był dotknięty chorobą Alzheimera i od 2000 roku znajdował się pod stałą opieką medyczną .
Z powodu stopnia uzyskanego w węgierskiej armii , Puskása określano również pseudonimem `` Galopujący major '' .
Puskás , występujący na pozycji łącznika , był zawodnikiem lewonożnym .
Lata młodzieńcze .
W wieku 12 lat Puskás porzucił szkołę i już jako nastolatek stał się profesjonalnym piłkarzem .
Klubową karierę rozpoczął w roku 1943 w klubie Kispest Budapest Honvéd FC .
Reprezentacja narodowa Węgier .
Przez wiele lat był kapitanem Złotej jedenastki .
Puskás grał w olimpijskiej reprezentacji Węgier , która zdobyła złoty medal na igrzyskach w 1952 roku .
W latach 1945–1956 Puskás wystąpił w zespole narodowym 85 razy i zdobył 84 bramki .
W 1954 roku awansował z drużyną do finału mistrzostw świata , w którym jego drużyna przegrała z Niemcami Zachodnimi 2:3 , mimo prowadzenia 2:0 ( pierwszego z goli dla Węgier strzelił właśnie Puskás ) .
Emigracja do Hiszpanii i gra w Realu Madryt .
Po upadku powstania węgierskiego Puskás opuścił w roku 1956 Węgry i wyemigrował do Hiszpanii .
W 1958 roku przeniósł się do Realu Madryt , z którym trzy razy zdobył Puchar Europy Mistrzów Krajowych w 1959 , 1960 i 1966 roku .
W 1960 , w finale z Eintrachtem Frankfurt zdobył 4 gole .
Puskas dokonał tego jako jedyny gracz w historii rozgrywek W latach 1961–1962 zaliczył również 4 występy reprezentacji Hiszpanii ( także podczas MŚ 1962 ) , jednak nie zdobył w nich gola .
Osiągnięcia i rekordy .
Łącznie w dwóch reprezentacjach rozegrał 89 spotkań międzypaństwowych i strzelił 84 bramki , co do 2003 , kiedy to pobił go piłkarz reprezentacji Iranu Ali Daei , było rekordem skuteczności na szczeblu reprezentacyjnym .
Jest jednak do dzisiaj rekordzistą pod względem strzelonych bramek w meczach towarzyskich .
W 78 meczach strzelił aż 76 goli .
