Feria del Sol
Feria del Sol ( `` Święto Słońca '' ) , znany także jako `` Carnaval Taurino de America '' – festiwal kulturalny w Mérida , który co roku odbywa się w Wenezueli .
Festiwal odbywa się lutym , w czasie karnawału .
Organizowanych jest wiele imprez kulturalnych , a także handlowe wystawy , koncerty , festiwale , parady i wybór królowej wystawy ( `` Reina del Sol '' ) .
Po wybudowaniu areny do walk byków , odbywają się walki tych zwierząt .
Historia .
Pierwotnie festiwal miał odbywać się na początku grudnia w czasie uroczystości Niepokalanego Poczęcia .
Pierwsze odbywały się w ciągu dwóch dni - 9 i 10 grudnia .
Ze względu na warunki atmosferyczne panujące w grudniu , postanowiono uczcić święta w okresie karnawału .
Pod nazwą `` Feria del Sol '' od 1969 imprezy organizowano w dniach 15 , 16 i 17 lutego .
Od tego czasu , festiwal jest jedną z najważniejszych w Wenezueli i na świecie imprezą walk byków .
