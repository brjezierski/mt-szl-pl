Fermín IV , właśc .
Fermín IV Caballero Elizondo ( ur .
w grudniu 1974 w Monterrey ) – meksykański raper , członek meksykańskiej grupy hip-hopowej Control Machete , pastor .
Życiorys .
W 1996 roku wstąpił do formacji Control Machete , której członkiem był do 2002 roku .
Po odejściu z zespołu zdecydował się na drogę religijną .
Obecnie jest żonaty , ma trzy córki i działa jako pastor w zborze chrześcijańskim Semilla de Mostaza w mieście Meksyk .
