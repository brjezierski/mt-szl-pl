Fernando Gago
Fernando Rubén Gago ( ur .
10 kwietnia 1986 w Ciudadeli ) – argentyński piłkarz posiadający także obywatelstwo włoskie , grający na pozycji defensywnego pomocnika w Boca Juniors .
Fernando jest wychowankiem argentyńskiego Boca Juniors , z którego przeniósł się do stolicy Hiszpanii za 20 mln euro .
W Realu zadebiutował 7 stycznia 2007 roku w meczu z Deportivo La Coruña , kiedy to na boisku spędził 58 minut .
W 2008 roku został powołany do prowadzonej przez Sergio Batistę reprezentacji U-23 na Igrzyska Olimpijskie w Pekinie , gdzie Argentyna zdobyła złoty medal .
Jego żoną jest Gisela Dulko ( argentyńska tenisistka , odnosząca sukcesy w grze deblowej ) .
