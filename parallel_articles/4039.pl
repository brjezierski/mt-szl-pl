Fernando Santos
Fernando Manuel Fernandes da Costa Santos ( ur .
10 października 1954 w Lizbonie ) – portugalski piłkarz i trener piłkarski , selekcjoner reprezentacji Polski w piłce nożnej .
Jako piłkarz Estoril Praia i CS Marítimo na pozycji obrońcy występował w Primeira Divisão .
Karierę trenerską rozpoczął wprowadzając Estoril Praia do Primeira Divisão .
Jako trener FC Porto , w sezonie 1998/1999 zdobył Mistrzostwo Portugalii , a następnie w sezonach 1999/2000 oraz 2000/2001 Puchar Portugalii .
W latach 1999–2000 dwukrotnie zdobył z klubem także Superpuchar Portugalii .
W sezonie 2000/2001 jako trener AEK Ateny zdobył z klubem Puchar Grecji .
Następnie trenował także drużyny Panathinaikosu AO , Sportingu CP , SL Benfiki oraz PAOK FC .
W latach 2010–2014 pełnił funkcję selekcjonera reprezentacji Grecji , którą poprowadził podczas Mistrzostw Europy 2012 i Mistrzostw Świata 2014 .
W latach 2014–2022 był selekcjonerem reprezentacji Portugalii , z którą zdobył Mistrzostwo Europy 2016 , wygrał rozgrywki Ligi Narodów UEFA 2018/2019 , sięgnął po brązowy medal Pucharu Konfederacji 2017 oraz prowadził podczas Mistrzostw Świata 2018 i 2022 oraz Mistrzostw Europy 2020 .
24 stycznia 2023 został selekcjonerem reprezentacji Polski .
Trener roku 2016 i 2019 na świecie według Międzynarodowej Federacji Historyków i Statystyków Futbolu .
Rodzice , młodość i wykształcenie .
Wychował się w Lizbonie .
Jego rodzice to Maria de Lurdes Fernandes Santos i Francisco Santos .
Ojciec Fernando był sprzedawcą akcesoriów samochodowych , miał także przemysł samochodowy , dlatego chciał , by jego syn został inżynierem .
Początkowo jego ojciec stawał na drodze do jego piłkarskiej kariery .
