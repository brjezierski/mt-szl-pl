Fernando Torres
Fernando José Torres Sanz ( ur .
20 marca 1984 w Fuenlabrada ) – hiszpański piłkarz , który występował na pozycji napastnika .
Reprezentant Hiszpanii , z którą zdobył Mistrzostwo Świata 2010 oraz Mistrzostwo Europy 2008 i 2012 .
Obecny trener Atlético Madrid Juvenil A .
Torres zaczął karierę w Atlético Madryt , w którym występował we wszystkich młodzieżowych drużynach .
W pierwszym zespole zadebiutował w 2001 , łącznie zagrał w 174 meczach Primera División , strzelając 75 goli .
Wcześniej przez dwa sezony reprezentował barwy klubu w Segunda División , zaliczając 40 spotkań , w których zdobył siedem bramek .
W 2007 odszedł do Liverpoolu stając się najdroższym piłkarzem w historii angielskiego klubu .
W debiutanckim sezonie zdobył ponad 20 goli w lidze , czego ostatnio dokonał Robbie Fowler w sezonie 1995/1996 .
W grudniu 2009 roku strzelił 50 bramkę w lidze i został najszybszym zawodnikiem , który tego dokonał .
W styczniu 2011 roku Torres przeszedł z Liverpoolu do Chelsea F.C .
za szóstą co do wysokości kwotę w historii futbolu 58,5 mln euro .
W 2015 roku został zawodnikiem klubu A.C. Milan , po czym ponownie reprezentował barwy ekipy ze stolicy Hiszpanii .
W 2018 roku przeszedł do Sagan Tosu .
W czerwcu 2019 zakończył karierę piłkarską .
Występował w reprezentacji Hiszpanii , w której zadebiutował w meczu przeciwko Portugalii w 2003 .
Z drużyną narodową wziął udział w Mistrzostwach Europy 2004 , Mistrzostwach Świata 2006 , Mistrzostwach Europy 2008 , Mistrzostwach Świata 2010 , Mistrzostwach Europy 2012 i Mistrzostwach Świata 2014 .
Nie strzelił gola na turnieju w 2004 , dwa lata później trzykrotnie trafił do siatki .
Na Euro 2008 zdobył dwie bramki , w tym zwycięską w wygranym 1:0 finale z Niemcami .
Cztery lata później strzelił trzy bramki , w tym jedną w finałowym meczu z Włochami .
Na Mistrzostwach Świata 2014 wystąpił w trzech meczach zdobywając jednego gola .
Dzieciństwo i początki kariery .
Urodzony 20 marca 1984 , jest trzecim dzieckiem Flori i José .
