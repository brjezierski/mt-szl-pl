Ferney-Voltaire
Ferney-Voltaire – miejscowość i gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Ain .
Demografia .
Według danych na styczeń 2012 r. gminę zamieszkiwały 9053 osoby , a gęstość zaludnienia wynosiła 1893,9 osób/km² .
Sławni pisarze .
W miejscowości znajduje się dawna posiadłość Woltera – pałac położony w parku .
Obecnie jest nadal własnością prywatną .
W centrum miasteczka znajduje się także pomnik upamiętniający pisarza .
