Fidel Castro
Fidel Alejandro Castro Ruz ( wym .
; ur .
13 sierpnia 1926 w Biránie , zm .
25 listopada 2016 w Hawanie ) – kubański rewolucjonista , polityk , żołnierz i adwokat .
Przywódca rewolucji kubańskiej .
Faktyczny przywódca Kuby w latach 1959–2011 .
Jedyny Kubańczyk w randze marszałka ( `` Comandante en Jefe '' ) .
W 1959 roku objął stanowisko premiera , a w 1965 sekretarza rządzącej Komunistycznej Partii Kuby .
W 1976 roku w wyniku reorganizacji rządu obok stanowiska premiera ( przemianowanego na Przewodniczącego Rady Ministrów ) , objął przewodnictwo Rady Państwa .
W latach 1979–1983 przewodniczący Ruchu państw niezaangażowanych .
W 2006 roku przekazał władzę młodszemu bratu Raúlowi .
W 2008 roku zrezygnował z najważniejszych stanowisk państwowych ze względu na zły stan zdrowia .
Edukację zdobył w szkołach jezuickich w Santiago de Cuba .
W kolejnych latach pracował w Hawanie jako adwokat .
