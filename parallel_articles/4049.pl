Fidenza ( stacja kolejowa )
Fidenza ( włoski : `` Stazione di Fidenza '' ) – stacja kolejowa w Fidenza , w prowincji Parma , w regionie Emilia-Romania , we Włoszech .
Znajduje się na linii Mediolan-Bolonia oraz na liniach Pontremolese , Cremona – Fidenza i Fidenza – Salsomaggiore Terme .
Obecny budynek pasażerski został zbudowany w latach dwudziestych według projektu inżyniera Enzo Bianchi .
Stacja posiada sześć torów w ruchu pasażerskim .
