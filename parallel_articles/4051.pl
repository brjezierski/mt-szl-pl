Status prawny .
Jednym z urzędowych języków na Fidżi , obok fidżyjskiego i angielskiego jest język hindustani ( nazwa obejmująca łącznie hindi i urdu ) , i jest to forma stosowana w szkolnictwie .
Natomiast właściwe hindi fidżyjskie służy głównie jako codzienny język mówiony , choć istnieje również literatura w tym języku .
