Hindi fidżyjskie
Hindi fidżyjskie ( hif . )
– język pochodzenia indoaryjskiego używany na wyspach Fidżi przez ok. 380 tys .
Hindofidżyjczyków , potomków przymusowych robotników zesłanych na przełomie XIX i XX w. na archipelag z północnoindyjskich stanów Uttar Pradesh i Bihar .
Hindi fidżyjskie wywodzi się głównie z języka bhodźpuri oraz dialektu hindi zwanego awadhi , ale zawiera również słowa o podłożu drawidyjskim , zaczerpnięte m.in .
z tamilskiego , telugu i malajalam .
Język ten obfituje również w pożyczki z angielskiego i fidżyjskiego .
Relacje pomiędzy hindi fidżyjskim a standardowym hindi są mniej więcej takie jak między afrikaans a niderlandzkim .
Wiele słów właściwych jedynie hindi fidżyjskiemu powstało zaraz po przybyciu osadników na wyspy dla wyrażenia nowych pojęć .
Pierwsze pokolenie użytkowników fidżyjskiego hindi nazywało ten dialekt `` Fiji Baat '' ( dosł .
„ mowa z Fidżi ” ) .
Większość ludności fidżyjskiej pochodzenia indyjskiego używa hindi fidżyjskiego również jako języka domowego , niezależnie od tego , jaki był język przodków .
Znane osoby pochodzące z rodzin fidżyjskich Hindusów to m.in .
piosenkarka Tanita Tikaram i gubernator Nowej Zelandii Anand Satyanand .
Fonetyka .
Fonetyka fidżyjskiego hindi nie odbiega od pierwotnego zasobu dźwięków w językach indyjskich .
Wykazuje typowe cechy dialektów bihari i wschodniego hindi , np .
zastąpienie ś przez s , oraz w przez b .
Gramatyka .
W zakresie gramatyki występują pewne różnice w stosunku do standardowego hindi .
Hindi fidżyjskie jest bowiem oparte na językach/dialektach wschodnich ( awadhi , magadhi , bhodźpuri , ćhattisgarhi ) .
