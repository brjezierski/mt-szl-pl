Fiesole
Fiesole – miejscowość i gmina we Włoszech , w regionie Toskania , w prowincji Florencja .
W roku 2004 gminę zamieszkiwały 14 142 osoby ( 336,7 os./km² ) .
Miasteczko leży w odległości ok. 8 km od Florencji na malowniczych wzgórzach , z których rozciąga się widok na Florencję i dominującą w jej panoramie katedrę Santa Maria del Fiore .
Fiesole ma rodowód starożytny , zostało założone przez lud Etrusków prawdopodobnie w VII wieku p.n.e .
