Opuncja figowa
Opuncja figowa ( `` Opuntia ficus-indica '' ) – gatunek opuncji pochodzący z Meksyku , lecz uprawiany również w wielu innych krajach o ciepłym klimacie , także w południowej Europie .
Łatwo przystosowuje się do różnych warunków glebowych .
Aztekowie uważali ją za roślinę świętą i wykorzystywali do różnych celów .
Pierwsze owoce , dotarły do Europy prawdopodobnie już pod koniec XV w. po powrocie wyprawy Krzysztofa Kolumba .
Stąd używana do tej pory nazwa `` ficus indica '' ( figa indyjska ) .
