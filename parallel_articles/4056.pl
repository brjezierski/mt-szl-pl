Filandia
Filandia – gmina w Kolumbii , w departamencie Quindío , na obszarze „ trójkąta kawowego ” ( Eje cafetero ) .
Położenie .
Filandia , stolica gminy , leży na wysokości 1910 m n.p.m. , 23 km na północ od stolicy departamentu Armenii i ma średnią roczną temperaturę 18 °C .
Filandia graniczy od północy z Pereirą w departamencie Risaralda , od południa z Circasia , od zachodu z Quimbaya i z Ulloa i Alcalą w departamencie Valle del Cauca , a na wschodzie z Salento i Circasia .
