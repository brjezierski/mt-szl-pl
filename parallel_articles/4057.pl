Filipiny
Filipiny , oficjalnie Republika Filipin ( fil .
`` Pilipinas '' , `` Republika ng Pilipinas '' ; , ; , ) – państwo wyspiarskie w południowo-wschodniej Azji , położone na Archipelagu Filipińskim na Oceanie Spokojnym .
Od północy Filipiny oblewane są wodami cieśniny Luzon , od zachodu wodami Morza Południowochińskiego .
Archipelag oddzielony od wyspy Borneo morzem Sulu oraz morzem Celebes od pozostałych wysp Indonezji .
Od wschodu Filipiny otoczone są wodami Morza Filipińskiego .
Położenie w strefie klimatu równikowego powoduje , że Filipiny narażone są na tajfuny .
Bliskość pacyficznego pierścienia ognia powoduje trzęsienia ziemi .
Jest to jeden z najbogatszych obszarów na świecie pod względem bioróżnorodności .
Cały archipelag składa się z kilku tysięcy wysp , ale większą część powierzchni zajmują Luzon i Mindanao , pomiędzy którymi znajdują się Visayas .
Stolicą kraju jest Manila – jedna z największych metropolii globu .
Filipiny mają ponad 100 mln mieszkańców i zajmują 12. miejsce na świecie pod względem liczby ludności .
