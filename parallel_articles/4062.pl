Finlandia
Finlandia , Republika Finlandii ( ; ) – państwo w Europie Północnej , powstałe po odłączeniu od Rosji w 1917 roku .
Członek Unii Europejskiej .
Graniczy od zachodu ze Szwecją , od północy z Norwegią i od wschodu z Rosją .
Od zachodu i południa ma ponadto dostęp do Morza Bałtyckiego i do Zatoki Fińskiej .
Zaliczana do krajów nordyckich .
Jej stolicą są Helsinki , które wraz z miastami Espoo i Vantaa tworzą obszar metropolitalny ; kolejnym największym obszarem miejskim jest Tampere , położone około 180 kilometrów na północ od Helsinek .
Niepodległa Finlandia powstała w 1917 , po upadku caratu w Imperium Rosyjskim .
Wcześniej była pod obcym panowaniem – szwedzkim od średniowiecza do roku 1809 , a następnie ponad 100 lat pod panowaniem rosyjskim , jako autonomiczne Wielkie Księstwo Finlandii .
