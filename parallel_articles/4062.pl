Klimat .
Południowa część Finlandii leży w strefie klimatu umiarkowanego chłodnego przejściowego .
Natomiast północ ( w tym Fińska Laponia ) jest w zakresie klimatu okołobiegunowego subpolarnego .
Średnie temperatury lipca na południu wynoszą od +17 do +18 °C i od +14 do +15 °C na północy ( w najchłodniejszym miejscu pod Kilpisjärvi +12 °C ) .
W styczniu średnie temperatury wynoszą –2 °C na Wyspach Alandzkich , –4 °C na południowym wybrzeżu , w środkowej części kraju od –8 do –10 °C i –14 °C na północy .
Długość okresu wegetacyjnego na południu wynosi 185-200 dni , na północy 120 .
Najwyższą temperaturę zanotowano w 1914 roku w Liperi , koło Joensuu i wynosiła ona +37,2 °C .
Nieoficjalnie najniższa temperatura wystąpiła w Kaaresuvanto ( –54 °C ) .
Okres zimowy występuje zwykle od połowy grudnia do końca marca na Wyspach Alandzkich , od końca listopada do końca marca na południowym wybrzeżu , od połowy listopada do połowy kwietnia w środkowej Finlandii i od końca października do końca kwietnia na północy .
Liczba dni w roku z temperaturą powyżej +25 °C waha się od 20-25 na południu do 5-7 na północy ( w górskiej stacji Kilpisjärvi takie temperatury występują średnio raz na 2 lata ) .
