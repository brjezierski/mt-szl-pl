Firlej ( Radom )
Firlej – dzielnica Radomia , znajdująca się w północnej części miasta , dawniej wieś .
Od północy graniczy z Wincentowem , od wschodu wzdłuż rzeki Mlecznej z Krzewieniem , Hutą Józefowską i dalej od południa z Józefowem , natomiast od zachodu wzdłuż ul .
Warszawskiej z Wólką Klwatecką .
Zabudowę stanowią przeważnie budynki jednorodzinne .
Większość obszaru dzielnicy zajmują lasy .
Historia .
Na terenie Firleja rodzina Beckermanów w XIX wieku założyła zakłady przemysłowe `` Firlej '' , które z czasem znalazły się w rękach rodziny Denów .
Przed II wojną światową znajdował się tu poligon wojskowy , który został przekształcony przez niemieckich okupantów w miejsce straceń – w pobliskim lesie wykonywane były wyroki śmierci na ludności polskiej i żydowskiej przez SS , Gestapo i żandarmów niemieckich .
