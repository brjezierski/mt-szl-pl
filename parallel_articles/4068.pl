Firn
Firn – forma przejściowa między śniegiem i lodem firnowym .
Gęstość : 0,4–0,8 g/cm3 .
Powstaje w wyniku przeobrażenia luźnych kryształów śniegu w agregaty ziaren lodu o średnicy dochodzącej do kilku milimetrów .
Proces ten zachodzi podczas wielokrotnego podtapiania , a następnie zamarzania śniegu .
Nie bez znaczenia jest też nacisk warstw nadległych – czyli kumulującego się śniegu .
