Ryby
Ryby – tradycyjna nazwa zmiennocieplnych , pierwotnie wodnych kręgowców , oddychających skrzelami i poruszających się za pomocą płetw .
Obejmuje bezżuchwowce krągłouste ( Cyclostomata ) oraz mające szczęki ryby właściwe ( Pisces ) .
Ryby stanowią najliczniejszą i najbardziej zróżnicowaną grupę współcześnie żyjących kręgowców ( ponad połowę ) .
Różnią się od siebie pod względem budowy zewnętrznej i wewnętrznej , ubarwienia oraz przystosowania do warunków środowiska .
Ponad 32 tysiące współcześnie żyjących gatunków opisano naukowo , a co roku naukowcy opisują 100–150 nowych gatunków morskich i nieco więcej słodkowodnych .
Szacuje się , że nie odkryto jeszcze co najmniej 5000 gatunków , głównie ryb głębinowych ze strefy klimatu tropikalnego .
W Polsce występuje około 120 gatunków .
Dział zoologii zajmujący się rybami to ichtiologia .
Występowanie .
Wszystkie typy zbiorników wodnych na Ziemi z wyjątkiem zbiorników o skrajnie trudnych warunkach .
Ryby występują we wszystkich strefach oceanów .
Największym bogactwem gatunków ryb wyróżnia się strefa otwartej toni wodnej , liczne gatunki występują również w strefie dennej .
