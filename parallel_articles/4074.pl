Fiľakovo
Fiľakovo ( węg .
Fülek , niem .
`` Fülleck '' ) – miasto na Słowacji w kraju bańskobystrzyckim , w powiecie Łuczeniec .
W 2011 roku liczyło 10 817 mieszkańców , z czego 53,5 % stanowili Węgrzy , 28,0 % Słowacy , 3,7 % Romowie .
Pierwsza pisemna wzmianka o miejscowości pochodzi z 1242 roku .
W Fiľakovie urodziła się Ľudmila Melicherová , rekordzistka Słowacji w maratonie .
