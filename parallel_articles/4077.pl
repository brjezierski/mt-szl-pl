Flags of the World
Flags of the World ( FOTW ) – organizacja weksylologiczna nieposiadająca fizycznej siedziby , bazująca na Internecie .
Równocześnie jest to portal z największą w sieci www bazą danych z dziedziny weksylologii , prowadzący także listę e-mailową .
FOTW założono w 1993 , w 2001 został przyjęty do Międzynarodowej Federacji Stowarzyszeń Weksylologicznych , formalnie jako organizacja kanadyjska .
