Płomienniczek rdzawobrązowy
Płomienniczek rdzawobrązowy ( `` Flammulaster erinaceellus '' ( Peck ) Watling ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Flammulaster , Tubariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungii '' .
Po raz pierwszy zdiagnozował go Charles Horton Peck w 1878 r. nadając mu nazwę `` Agaricus erinaceellus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Roy Watling w 1986 r .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 1999 r .
Morfologia .
Średnica 1–4 cm , początkowo wypukły , potem płaski .
Powierzchnia sucha , ciemnobrązowa lub rdzawobrązowa , gęsto pokryta ziarnistymi łuskami , łatwo obkruszającymi się .
Na brzegu często zwisające resztki osłony .
