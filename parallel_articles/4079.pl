Żółtoporek tropikalny
Żółtoporek tropikalny ( `` Flaviporus brownii '' ( Humb . )
Donk ) – gatunek grzybów należący do rodziny ząbkowcowatych ( `` Steccherinaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Flaviporus , Steccherinaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1793 r. Alexander von Humboldt , nadając mu nazwę `` Boletus brownii '' .
Potem zaliczany był do różnych innych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadał w 1960 r. Marinus Anton Donk , przenosząc go do rodzaju `` Flaviporus '' .
