Położna ( skała )
Położna – skała w prawych zboczach Doliny Brzoskwinki w miejscowości Chrosna w województwie małopolskim , w powiecie krakowskim , w gminie Liszki .
Znajduje się na Garbie Tenczyńskim ( część Wyżyny Krakowsko-Częstochowskiej ) w obrębie Tenczyńskiego Parku Krajobrazowego .
W 2007 r. na prawym zboczu Doliny Brzoskwinki usunięto zadrzewienia na skałach i w ich otoczeniu , przez co skały stały się dobrze widoczne i efektowne .
Tuż poniżej Położnej prowadzi szlak turystyczny i ścieżka dydaktyczna .
Od północno-wschodniej strony Położna przylega do skały Obchodniej .
Oddzielone są szeroką szczeliną skalną z kominem .
Zbudowana z wapienia Położna ma wysokość 18–22 m , pionowe lub połogie ściany z filarem i jest obiektem wspinaczki .
Jest na niej 7 dróg wspinaczkowych o trudności V- do VI.2 w skali Kurtyki ) i długości 18–22 m. Wszystkie mają zamontowane stałe punkty asekuracyjne w postaci 4–8 ringów ( r ) i stanowiska zjazdowe ( st ) lub dwa ringi zjazdowe ( drz ) .
Ściany wspinaczkowe o wystawie wschodniej i południowo-wschodniej .
