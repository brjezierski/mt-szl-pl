Fleurier
Fleurier – miejscowość i gmina w Szwajcarii , w kantonie Neuchâtel w dystrykcie Val-de-Travers .
Powierzchnia 7.74 km² .
Około 3,500 mieszkańców ( 2007 r. ) .
W gminie znajdują się dwie luksusowe firmy zegarmistrzowskie Bovet Fleurier oraz Parmigiani Fleurier .
