Samolot
Samolot – załogowy bądź bezzałogowy statek powietrzny cięższy od powietrza ( aerodyna ) , utrzymujący się w powietrzu dzięki sile nośnej wytwarzanej za pomocą nieruchomych , w danych warunkach , względem statku skrzydeł .
Ciąg potrzebny do utrzymania prędkości w locie poziomym wytwarzany jest przez jeden lub więcej silników .
