Historia samolotu .
Ludzie od niepamiętnych czasów marzyli o lataniu .
Na podstawie obserwacji ptaków Leonardo da Vinci ( 1452–1519 ) zaprojektował pierwszą lotnię .
Pionierzy latania , tacy jak Niemiec Otto Lilienthal ( 1848–1896 ) , udowodnili , że szybowiec może wynieść człowieka w powietrze .
Pod koniec XIX w. wielu wynalazców próbowało skonstruować samolot , czyli szybowiec z napędem .
Udało się to dwóm Amerykanom braciom Orville ’ owi ( 1871–1948 ) i Wilburowi ( 1867–1912 ) Wrightom , którzy zbudowali samolot poruszany silnikiem tłokowym i w 1903 roku dokonali pierwszego kontrolowanego przelotu .
Trwał on 12 sekund .
Tego samego dnia bracia dokonali kolejnych trzech przelotów , w których samolot przeleciał 270 metrów , a najdłuższy lot trwał 59 sekund .
