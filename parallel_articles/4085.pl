Flint ( Walia )
Flint ( wal .
`` Y Fflint '' ) – miasto w północnej Walii ( Wielka Brytania ) , hrabstwo Flintshire ( wraz z Mold są największymi miastami hrabstwa ) .
Graniczy od wschodu z Cheshire oraz okręgiem Wrexham na południu i Denbighshire na zachodzie .
Według spisu powszechnego z 2001 roku miasto liczy 11 936 mieszkańców .
Historia .
Początki współczesnego Flint sięgają XIII w. , gdy król Edward I , wzniósł nad zatoką warownię z zamkiem , zniszczoną w 1646 r. przez Armię Parlamentarną podczas wojny domowej .
