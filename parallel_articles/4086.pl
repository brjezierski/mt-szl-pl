Flipper ( zespół muzyczny )
Flipper – amerykański zespół punkrockowy z San Francisco utworzony w 1979 przez byłych członków zespołu Negative Trend : basistę Willa Shattera i perkusistę Steve DePace .
Wkrótce dołączyli do nich : wokalista Bruce Loose i gitarzysta Ted Falconi .
Pierwszych nagrań dokonali pod koniec 1979 na mini składak „ SF Underground ” ( utwór `` Earthworm '' ) .
W 1980 ukazał się debiutancki singel : `` Love Canal '' .
W następnych latach wyszły jeszcze dwa studyjne albumy : `` Album – Generic Flipper '' ( 1982 ) i `` Gone Fishin '' ' ( 1984 ) .
Muzyka jaką Flipper zaprezentował była pełna „ brudu ” , hałasu i chaosu .
Muzycy byli często posądzani o brak umiejętności grania .
Zespół prawie zawsze trafiał na wrogo nastawioną publiczność , ale równocześnie umiał przykuć jej uwagę i wzbudzić w niej ciekawość .
Pierwszy singel zespołu `` Love Canal '' został przez wszystkich wyśmiany za zawarte na nim dziwne dźwięki ( choć sprzedawał się dosyć dobrze ) .
Zespół był znany również z tego , że w całym San Francisco ( nawet już długo po ich rozpadzie ) widać było na murach budynków ( w formie graffiti ) logo zespołu , które przedstawiało ryby .
W 1984 roku firma ROIR wydała kasetę z koncertem Flippera nagranym w klubie CBGB w Nowym Jorku pt .
`` Blow ’ n Chunks '' .
Działalność zespołu w tym czasie stopniowo zanikała .
W 1987 roku w wyniku przedawkowania narkotyków umarł Shatter .
