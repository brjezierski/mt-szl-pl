Kosmkówka żółtawa
Kosmkówka żółtawa ( `` Floccularia luteovirens '' ( Alb .
& amp ; Schwein . )
Pouzar ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Agaricaceae , Incertae sedis , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1805 r. Johannes Baptista von Albertini i Lewis David von Schweinitz nadając mu nazwę `` Agaricus luteovirens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1957 r. Zdeněk Pouzar .
Gatunek ten ma ponad 20 synonimów naukowych .
Niektóre z nich :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. ( dla synonimu `` Floccularia straminea '' ) .
Morfologia .
Grzyb wytwarzający jadalne owocniki z oliwkowożółtawym lub brązowawym kapeluszem ( średnicy przeważnie 4–8 cm ) o lekko filcowatej , łuskowatej lub spękanej , suchej skórce na górnej powierzchni i brudnobiaławym blaszkowatym hymenoforze na spodzie .
Kapelusz umieszczony jest na żółtawym lub brązowawym ( u młodych owocników białawym ) trzonie , a miąższ jest początkowo biały , u dojrzałych owocników żółty lub żółtobrązowy , o łagodnym smaku i charakterystycznej woni selera lub lubczyku .
Występowanie i siedlisko .
W piśmiennictwie naukowym na terenie Polski do 2003 r. opisano tylko jedno stanowisko ( Andrzej Nespiak pod kosodrzewiną w Tatrzańskim Parku Narodowym w 1953 r. ) .
Według W. Wojewody jest to gatunek w Polsce wymarły .
Gatunek ten występuje w lasach iglastych ( pod sosnami i świerkami ) .
Owocniki wytwarza od sierpnia do października , mogą być one stosowane w celach przyprawowych .
