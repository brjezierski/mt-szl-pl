Floriana ( stolica tytularna )
Floriana ( łac .
`` Diocesis Florianensis '' ) – stolica historycznej diecezji we Cesarstwie rzymskim w prowincji Mauretania Cezarensis , zlikwidowana w VII w. podczas ekspansji islamskiej , współcześnie w północnej Algierii .
Obecnie katolickie biskupstwo tytularne .
