Florianópolis
Florianópolis ( zwane również Floripa ) – miasto w południowej Brazylii , stolica stanu Santa Catarina .
Miasto obejmuje obecnie całą wyspę Świętej Katarzyny oraz dzielnicę Capoeiras na stałym lądzie .
Liczba ludności wynosi 500 973 osób ( 2019 ) .
Historia .
Osada została założona w 1673 r. pod nazwą Desterro , a w 1714 r. uzyskała prawa miejskie .
