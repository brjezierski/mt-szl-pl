Floryda
Floryda ( ang .
`` State of Florida '' ) ( ) – stan na południowym wschodzie Stanów Zjednoczonych , na półwyspie Floryda otoczonym przez wody Zatoki Meksykańskiej na zachodzie i Oceanu Atlantyckiego na wschodzie .
Floryda graniczy na północy z Alabamą i Georgią .
Floryda jest trzecim pod względem liczby ludności ( 21,7 mln ) i ósmym pod względem gęstości zaludnienia ( 121 mieszk./km² ) stanem USA .
Obszar metropolitalny Miami , liczący ponad 6 mln mieszkańców , jest największą aglomeracją Florydy i dziewiątą co do wielkości w Stanach Zjednoczonych .
Inne duże aglomeracje to : Tampa ( 3,2 mln ) , Orlando ( 2,6 mln ) i Jacksonville ( 1,5 mln ) .
Stolicą stanu jest Tallahassee .
Floryda to najpopularniejszy cel podróży na świecie , a turystyka wnosi do gospodarki stanu ponad 65 miliardów dolarów rocznie .
Stan znany jest jako `` Sunshine State '' ( Słoneczny Stan ) .
Geografia .
Teren jest nizinny , płaski lub pofałdowany , liczne jeziora i bagna , głównie na południu ( Everglades ) .
Lasy zajmują około połowy obszaru Florydy ; większość z nich leży na północ od Orlando .
Płaskowyż na północy Florydy to zazwyczaj otwarte lasy zdominowane przez sosny .
Floryda jest w większości położona na wielkim półwyspie pomiędzy Zatoką Meksykańską , Oceanem Atlantyckim i Cieśniną Florydzką .
Graniczy ze stanami Georgia oraz Alabama na północy .
Floryda leży niedaleko Karaibów , Kuby i Wysp Bahama .
Rozległy pas przybrzeżny Florydy był postrzegany przez rząd Stanów Zjednoczonych jako potencjalne miejsce ataku w trakcie II wojny światowej , dlatego wybudowano lądowiska i pasy startowe na terenie całego stanu oraz dookoła niego .
