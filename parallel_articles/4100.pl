Włostowa ( góra )
Włostowa ( 903 m n.p.m . )
– wzniesienie w Sudetach Środkowych , w Górach Kamiennych .
Położenie .
Wzniesienie położone w północno-zachodniej części pasma Gór Suchych , około 3,7 km na południowy zachód od miejscowości Rybnica Leśna .
Jest to góra w kształcie stożka , o stromych zboczach , z kopulastą częścią szczytową w kształcie małego grzbietu , z wyraźnie zaznaczonymi dwoma małymi wzniesieniami szczytowymi .
Zbocze północno-wschodnie , w części grzbietowej łagodnie opada , a po osiągnięciu około 25 m różnicy poziomu względem wierzchołka , przechodzi w zachodnie zbocze Kostrzyny ( 906 m n.p.m . ) .
Poniżej szczytu u podnóża południowego zbocza na wysokości około 640 m n.p.m .
oraz w części grzbietowej wzniesienia znajdują się grupy skałek .
Jest to wzniesienie zbudowane ze skał wylewnych - porfirów ( trachitów ) , należących do północnego skrzydła niecki śródsudeckiej .
Wzniesienie w całości porośnięte jest lasem regla dolnego .
Leży na obszarze Parku Krajobrazowego Sudetów Wałbrzyskich .
