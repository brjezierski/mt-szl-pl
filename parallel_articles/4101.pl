Flota Świnoujście
Flota Świnoujście – polski klub piłkarski z siedzibą w Świnoujściu .
Ćwierćfinalista Pucharu Polski w 2013 roku .
Przez siedem sezonów w latach 2008−2015 drużyna występowała w piłkarskiej I lidze .
Od sezonu 2021/22 drużyna występuje w IV lidze .
Historia .
Klub został założony 17 kwietnia 1957 roku w Świnoujściu jako Wojskowy Klub Sportowy „ Flota ” .
Pierwszym prezesem był Marian Mamorski .
W 1968 roku , po rozwiązaniu WKS , na jego bazie powstał Międzyzakładowy Klub Sportowy „ Flota ” , który z kolei w 1986 przekształcił się w Miejski Klub Sportowy „ Flota ” .
19 lipca 2014 właściciel klubu – urząd miasta – zdecydował o wycofaniu Floty z rozgrywek I ligi i sprzedaży licencji Stali Rzeszów .
22 lipca Polski Związek Piłki Nożnej unieważnił jednak umowę pomiędzy klubami i zespół pozostał na zajmowanej pozycji .
PZPN powołał się przy tym na jeden z przepisów podręcznika licencyjnego .
Problemy finansowe Floty nawarstwiały się , aż ostatecznie 13 maja 2015 roku drużyna została wycofana z rozgrywek .
Na nowo Flota pod nazwą Morski Klub Sportowy „ Flota ” została zarejestrowana 20 czerwca 2015 roku .
Prezesem klubu został Leszek Zakrzewski .
W klubie rozpoczęły działalność cztery drużyny piłkarskie : seniorzy ( klasa A ) , juniorzy starsi ( liga wojewódzka ) oraz dwa zespoły trampkarzy .
Stadion .
Flota swoje domowe mecze rozgrywa na Stadionie Miejskim OSiR w Świnoujściu .
