Flower Mound
Flower Mound – miasto w Stanach Zjednoczonych , w stanie Teksas , w hrabstwach Denton i Tarrant .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 roku miasto liczyło 64 669 mieszkańców .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 83,9 % , Afroamerykanie 3,2 % , Azjaci 8,6 % , pozostali 4,3 % .
