Fluor
Fluor ( F , od fluorytu ) – pierwiastek chemiczny , niemetal z grupy fluorowców w układzie okresowym .
Fluor w stanie wolnym występuje w postaci dwuatomowej cząsteczki .
Jest żółtozielonym silnie trującym gazem o ostrym zapachu podobnym do chloru .
Jest najaktywniejszym niemetalem o największej elektroujemności , tworzącym związki z większością innych pierwiastków ( nawet z gazami szlachetnymi – kryptonem , ksenonem i radonem ) .
W przeciwieństwie do innych fluorowców fluor łączy się wybuchowo z wodorem , tworząc fluorowodór bez dostępu światła i w niskiej temperaturze .
W strumieniu gazowego fluoru palą się szkło , metale i woda .
Z powodu jego dużej aktywności nie można go przechowywać ani wytwarzać w naczyniach szklanych .
