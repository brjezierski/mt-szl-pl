Otrzymywanie .
Zarówno w skali przemysłowej , jak i laboratoryjnej , fluor otrzymuje się praktycznie wyłącznie poprzez elektrolizę ciekłej mieszaniny fluorowodoru i fluorku potasu .
Pierwotnie stosowano mieszaninę bogatą w HF i temperaturę niższą od pokojowej , później opracowano proces wysokotemperaturowy , w którym elektrolit miał skład KF·HF i temperaturę topnienia 239 °C .
Metoda ta dominowała w pierwszej połowie XX w. Obecnie przeważa proces średniotemperaturowy , prowadzony w 80–110 °C , z elektrolitem o składzie KF·2HF ( ttopn .
= 71,7 °C ) .
Zastosowanie .
Gazowego fluoru używa się przy produkcji monomerów , fluorowanych alkenów , z których otrzymuje się teflon i jego pochodne .
Oprócz tego jest stosowany do produkcji halonów , które są stosowane jako ciecze chłodzące i hydrauliczne ( np .
freon ) .
