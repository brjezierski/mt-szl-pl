Flet
Flet – instrument dęty drewniany z grupy aerofonów wargowych .
Zazwyczaj ma postać cienkiej , pustej w środku rurki ( istnieją również flety o innych kształtach , np .
okaryna ) .
Najbardziej znanymi odmianami fletów są flet poprzeczny ( mniejszy – flet piccolo ) , popularny instrument muzyki klasycznej , oraz flet prosty , instrument popularny w muzyce artystycznej do XVIII w. , obecnie wykorzystywany do wykonywania muzyki dawnej , muzyki współczesnej pisanej na ten instrument , na zajęciach wychowania muzycznego w szkołach podstawowych , a nawet do leczenia astmy .
Do XVIII w. nazwa „ flet ” ( `` flauto '' ) oznaczała flet prosty , podczas gdy flet poprzeczny był znany jako `` traverso '' .
Historia .
Flet jest instrumentem używanym w wielu formach , miejscach i kulturach na świecie od czasów prehistorycznych .
