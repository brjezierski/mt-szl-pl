Zapalniczka ( film )
Zapalniczka – polski film kryminalny z 1970 roku w reżyserii Krzysztofa Szmagiera .
Film kręcony w Warszawie ( Rynek Starego Miasta , Lotnisko Chopina ) .
Treść .
Młody naukowiec Jacek dostaje zaproszenie na sympozjum naukowe w Wiedniu .
Jeden z kolegów naukowców pożycza mu walizkę .
Na miejscu okazuje się , że w walizce ukryto 10000 dolarów .
Wkrótce potem właściciel walizki ginie w wypadku samochodowym .
Na miejscu tego wypadku zostaje znaleziona zapalniczka .
