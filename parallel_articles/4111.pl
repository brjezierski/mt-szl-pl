Folgowo
Folgowo – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie chełmińskim , w gminie Papowo Biskupie .
Wieś zajmuje powierzchnię 336,5 ha .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa toruńskiego .
Historia .
Średniowiecze .
Pierwsza wzmianka o miejscowości pochodzi z 1438 .
W kronikach i dokumentach wieś była nazywana : Hillebrandisdorff ( 1438 ) , Hylmensdorff ( 1438 ) , Hildebrandisdorff ( 1438 ) , Folgowo ( 1570 ) , Folgowo ( 1614 ) .
