Fontainebleau
Fontainebleau – miasto i gmina we Francji , w regionie Île-de-France , w departamencie Sekwana i Marna , 60 km na południowy wschód od Paryża , nad Sekwaną .
Według danych na rok 1999 gminę zamieszkiwały 15 942 osoby , a gęstość zaludnienia wynosiła 93 osób/km² .
Wśród 1287 gmin regionu Île-de-France Fontainebleau plasuje się na 185. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 1 .
Etymologia .
Pochodzenie nazwy Fontainebleau nie jest jasne .
