Fontaines ( Wandea )
Fontaines – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Wandea .
W 2013 roku populacja ludności wynosiła 806 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Doix oraz Fontaines – utworzono nową gminę Doix-lès-Fontaines .
Siedzibą gminy została miejscowość Doix .
