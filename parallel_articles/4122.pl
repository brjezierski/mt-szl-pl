Forlì ( stacja kolejowa )
Forlì ( wł : `` Stazione di Forlì '' ) – stacja kolejowa w Forlì , w regionie Emilia-Romania , we Włoszech .
Znajdują się tu 2 perony .
Jest zarządzana przez Centostazioni i Rete Ferroviaria Italiana .
Ruch .
Obsługa pasażerów odbywa się wyłącznie przez Trenitalia ( spółki zależnej Ferrovie dello Stato ) w imieniu regionu Emilia-Romania .
Pociągi obsługujące stację to : Regionalne , Express , Intercity i Eurostar City .
W sumie około sto pięć pociągów obsługuje stację każdego dnia .
Główne kierunki to : Bologna Centrale , Ankona , Rimini , Mediolan , Piacenza .
Około 2 milionów 800 tysięcy pasażerów rocznie obsługuje stacja .
