Forney
Forney – miasto w Stanach Zjednoczonych , w stanie Teksas , w hrabstwie Kaufman .
Zostało nazwane na cześć urzędnika przechodzącej przez ten obszar kolei , a prawa miejskie otrzymało w 1884 roku .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 roku miasto liczyło 14 661 mieszkańców .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 80,8 % , Afroamerykanie 10,0 % , Azjaci 1,1 % , pozostali 8,1 % .
