Forrest Edward Mars
Forrest Edward Mars ( ur .
21 marca 1904 w Tacoma , zm .
1 lipca 1999 w Miami ) – amerykański przedsiębiorca .
Syn Franklina Marsa , założyciela firmy produkującej słodycze Mars Incorporated .
Najbardziej znany ze stworzenia marki czekoladowych cukierków M & amp ; M 's oraz ryżu Uncle Ben 's .
Z małżeństwa z Audrey ( zmarła w 1989 ) miał troje dzieci : synów Foresta i Johna , oraz córkę Jacqueline .
Forrest Edward Mars zmarł w wieku 95 lat , zostawiając po sobie majątek o wartości 4 miliardów dolarów .
Został sklasyfikowany na 30 miejscu listy najbogatszych Amerykanów magazynu `` Forbes '' .
