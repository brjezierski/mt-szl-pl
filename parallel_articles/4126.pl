Forsbacka
Forsbacka – miejscowość ( `` tätort '' ) w Szwecji , w regionie administracyjnym ( `` län '' ) Gävleborg ( gmina Gävle ) .
Miejscowość położona jest w prowincji historycznej ( `` landskap '' ) Gästrikland nad jeziorem Storsjön i odwadniającą je rzeką Gavleån , ok. 10 km na wschód od Sandviken przy drodze E16 w kierunku Gävle .
Historia Forsbacka związana jest z metalurgią .
Obróbką metali zajmowano się tam od co najmniej końca XVI w. Decydujące znaczenie miało tutaj korzystne położenie miejscowości , przy rzece Gavleån i w stosunkowo niewielkiej odległości od portu w Gävle .
Pod koniec lat 40 .
XX w. zakłady metalurgiczne w Forsbacka ( `` Forsbacka Bruk '' ) zatrudniały ponad 1000 osób .
W połowie lat 80 .
XX w. podjęto decyzję o zamknięciu zakładów .
W 1992 r. większość zachowanych budynków przemysłowych przekazano fundacji Forsbacka Bruk ( `` Stiftelsen Forsbacka Bruk '' ) i udostępniono zwiedzającym .
W 1998 r. działalność fundacji została wyróżniona nagrodą federacji Europa Nostra .
W 2010 r. Forsbacka liczyła 1702 mieszkańców .
