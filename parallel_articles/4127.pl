Forst ( powiat Karlsruhe )
Forst – gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Mittlerer Oberrhein , w powiecie Karlsruhe , wchodzi w skład wspólnoty administracyjnej Bruchsal .
Leży ok. 20 km na północny wschód od Karlsruhe , przy autostradzie A5 i linii kolejowej Stuttgart – Mannheim .
