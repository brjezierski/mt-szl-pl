Fort Lauderdale
Fort Lauderdale – miasto w Stanach Zjednoczonych , w południowo-wschodniej części stanu Floryda .
Port nad Oceanem Atlantyckim .
Jest jednym z centrów obszaru metropolitalnego Miami .
Według spisu w 2020 roku liczy 182,8 tys .
mieszkańców .
Miasto rozwijało się jako ośrodek żeglugowy , handlowy oraz wypoczynkowy .
Historia .
Obszar ten był pierwotnie zamieszkany przez Indian Tequesta , którzy zniknęli , gdy pojawili się pierwsi odnotowani osadnicy około 1788 roku .
