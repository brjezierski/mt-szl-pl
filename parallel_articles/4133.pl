Fort Worth
Fort Worth – miasto w Stanach Zjednoczonych , w stanie Teksas , w regionie metropolitalnym Dallas–Fort Worth , nad rzeką Trinity .
Miasto Fort Worth zostało założone w 1849 roku jako placówka wojskowa na urwisku z widokiem na rzekę Trinity .
Historycznie było centrum handlu bydłem rasy Texas Longhorn .
Znaleziska ropy spowodowały rozwój przemysłu rafineryjnego w latach dwudziestych XX wieku , a podczas II wojny światowej rozpoczęto produkcję samolotów .
Swoją siedzibę ma tutaj American Airlines , największa linia lotnicza na świecie i największy pracodawca w mieście , zatrudniający w 2019 roku ponad 5,5 tys .
