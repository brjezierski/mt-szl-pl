XVII-wieczne , francuskie osadnictwo na wyspie koncentrowało się na jej zachodnim , karaibskim wybrzeżu .
Jedną z głównych osad było założone w 1672 Fort-Royal , dzisiejsze Fort-de-France .
Fort , założony u wejścia do największej zatoki wyspy , stanowił doskonałe stanowisko obrony , zarówno przeciwko miejscowym ludom karaibskim , jak i kolonistom holenderskim i angielskim .
Położenie miasta nie miało jednak wyłącznie zalet .
Otoczenie bagnami sprzyjało rozwojowi malarii .
W XIX wieku dołączyły do tego inne klęski żywiołowe : trzęsienie ziemi 11 stycznia 1839 , wielki pożar miasta 22 czerwca 1890 i huragan z 18 sierpnia 1891 .
Fort-de-France przez cały ten okres stanowiło jednak główny ośrodek administracyjny i militarny Martyniki .
