Fowey
Fowey ( korn .
`` Fowydh '' ) – miasto w Wielkiej Brytanii , w Anglii , w hrabstwie Kornwalia przy ujściu rzeki Fowey do kanału La Manche Niewielki port i przystań .
Miasto znajduje się na terenie parku krajobrazowego .
W przeszłości centrum wypalania porcelany i wyrobów glinianych .
Obecnie modna przystań jachtów i regionalny ośrodek turystyki wodnej .
Turystyka .
W Fowey zaczyna się ( lub kończy ) szlak turystyczny Saints ' Way o długości ok. 40 km do Padstow , przecinający Półwysep Kornwalijski .
