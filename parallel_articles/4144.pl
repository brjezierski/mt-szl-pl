Foz do Iguaçu
Foz do Iguaçu – miasto w Brazylii będące szóstym pod względem wielkości organizmem miejskim stanu Parana .
Liczba mieszkańców wraz z obrzeżami wynosi 270 000 mieszkańców .
Miasto leży około 500 km na zachód od Kurytyby będącej stolicą stanu .
Odległość drogowa od Rio de Janeiro wynosi 1500 km , a od São Paulo 1050 km .
Mieszkańcy miasta są nazywani iguaçuenses .
Około 20 km na północ od miasta znajduje się wielka zapora Itaipu , niedaleko jest też do wodospadów Iguaçu .
Wybudowany w latach 1960-1965 Most Przyjaźni łączy miasto z leżącym na przeciwległym brzegu rzeki Parana paragwajskim miastem Ciudad del Este .
