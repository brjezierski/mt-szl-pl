Francesco Cossiga
Francesco Cossiga ( ur .
26 lipca 1928 w Sassari , zm .
17 sierpnia 2010 w Rzymie ) – włoski prawnik i polityk , były premier i prezydent kraju , wieloletni parlamentarzysta .
Życiorys .
Z wykształcenia prawnik .
Pracował zawodowo jako nauczyciel akademicki na Uniwersytecie w Sassari , gdzie wykładał prawo konstytucyjne .
Od młodości związany z Chrześcijańską Demokracją , do której wstąpił w 1945 .
W 1958 został po raz pierwszy wybrany do Izby Deputowanych .
Posłem pozostawał nieprzerwanie do 1983 w ramach III , IV , V , VI , VII i VIII kadencji .
W 1966 objął stanowisko podsekretarza stanu w resorcie obrony , które zajmował w kilku rządach .
W 1974 Aldo Moro powierzył mu urząd ministra bez teki , a w 1976 urząd ministra spraw wewnętrznych .
Funkcję tę pełnił także w gabinecie Giulia Andreottiego .
Ustąpił w 1978 po zamordowaniu Aldo Moro , za co przyjął polityczną odpowiedzialność .
Porwanie i zabójstwo byłego premiera nie zachwiało jednak jego karierą polityczną .
