Francesco Totti
Francesco Totti ( ur .
27 września 1976 w Rzymie ) – włoski piłkarz występujący na pozycji napastnika , przez całą karierę związany z AS Roma .
Wychowanek i wieloletni kapitan tego klubu .
W latach 1998–2006 reprezentant Włoch , mistrz świata z 2006 roku i wicemistrz Europy z 2000 roku .
Totti trafił do Romy w 1989 roku .
Od 1992 roku występował w pierwszej drużynie tego klubu .
Z zespołem `` Giallorossich '' zdobył mistrzostwo Włoch , 2 Puchary Włoch i 2 Superpuchary Włoch .
Jest najlepszym strzelcem i zawodnikiem z największą liczbą występów w historii tego klubu .
