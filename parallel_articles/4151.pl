Paco Alcácer
Francisco „ Paco ” Alcácer García ( ur .
30 sierpnia 1993 w Torrent ) – hiszpański piłkarz , występujący na pozycji napastnika w emirackim klubie Szardża FC , do którego jest wypożyczony z Villarreal CF oraz w reprezentacji Hiszpanii .
Kariera klubowa .
Alcácer jest wychowankiem Valencii .
W sezonie 2009/10 , w wieku 16 lat zadebiutował w rezerwach , trafił trzy bramki w 15 spotkaniach , a jego drużyna spadła z trzeciej ligi .
11 listopada 2010 zadebiutował w pierwszej drużynie , przeciwko Logroñés w Pucharze Hiszpanii .
12 sierpnia 2011 trafił bramkę w meczu towarzyskim przeciwko Romie .
Po meczu , gdy opuszczał stadion wraz z rodzicami , jego ojciec miał atak serca i upadł na ziemię .
