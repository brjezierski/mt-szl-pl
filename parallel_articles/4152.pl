Franciszek Fesser
Franciszek Fesser ( ur .
16 sierpnia 1885 w Rogach , zm .
23 października 1956 w Katowicach-Piotrowicach ) – polski górnik , działacz związkowy i polityczny na Górnym Śląsku , powstaniec , poseł na Sejm Śląski III i IV kadencji ( 1930–1939 ) .
Życiorys .
Urodził się 16 sierpnia 1885 w Rogach , w rodzinie Franciszka ( 1838–1899 ) i Józefy z d. Janocha ( 1847–1917 ) .
W młodości pracował jako górnik w kopalni „ Kleofas ” .
W 1918 przyłączył się do Polskiej Organizacji Wojskowej w rodzinnym powiecie opolskim .
Wziął udział w akcji plebiscytowej oraz II i III powstaniu śląskim .
Po włączeniu wschodniej części Śląska do RP w 1922 znalazł się w Polsce , gdzie działał w organizacjach narodowo–patriotycznych , m.in .
Związku Powstańców Śląskich oraz Związku Obrony Kresów Zachodnich .
Działał w ruchu związkowym , organizował Związek Związków Zawodowych na Śląsku , a od 1930 był prezesem Związku Zawodowego Przemysłu Górniczego ( odłamu ZZZ ) .
W 1926 związał się z Narodowo-Chrześcijańskim Zjednoczeniem Pracy , z ramienia którego uzyskał mandat posła do Sejmu Śląskiego III kadencji ( 1930 ) .
Zasiadał w Komisji Pracy i Opieki Społecznej .
W 1935 uzyskał reelekcję z tego samego ugrupowania .
Pracował w Komisjach Pracy i Opieki Społecznej ( jako wiceprzewodniczący ) oraz : Administracyjno-Samorządowej , Regulaminowej i Budżetowo-Skarbowej .
W latach 30. pełnił również funkcję zastępcy naczelnika gminy Piotrowice .
W czasie okupacji niemieckiej przebywał w Polsce centralnej .
Po 1945 wrócił na Śląsk , jednak nie uczestniczył w życiu politycznym ( m.in .
z powodu choroby ) .
Zmarł w Katowicach-Piotrowicach .
Był mężem Barbary z d. Hupasch ( 1883–1955 ) .
