Franciszek Honiok
Franciszek Honiok ( ur .
1896 , zm .
31 sierpnia 1939 w Gliwicach ) – uczestnik powstań śląskich , zamordowany podczas prowokacji gliwickiej , rozpoczynającej niemiecki plan inwazji na Polskę , jedna z pierwszych ofiar II wojny światowej .
Życiorys .
Urodził się w 1896 roku na Górnym Śląsku .
Miał obywatelstwo niemieckie , był katolikiem i nie miał żony .
Mieszkał we wsi Łubie ( ówczesnie Hohenlieben ) , leżącej na ówczesnych terenach III Rzeszy .
Opisany został jako niewyróżniający się , niski rolnik .
W latach 20 .
XX wieku mieszkał w Polsce , w roku 1921 walczył po stronie polskiej podczas powstań śląskich , które nastąpiły po I wojnie światowej .
Inne źródła podają , że tylko sympatyzował z Polakami .
Wrócił do Niemiec w 1925 roku , gdzie został zmuszony do walki z deportacją z powrotem do Polski , co robił aż do 1939 roku .
W latach 30. opowiadał się po stronie Polaków .
W marcu 1938 wziął udział w odbywającym się w Berlinie kongresie Związku Polaków w Niemczech .
Aresztowanie i śmierć .
30 sierpnia 1939 roku został zatrzymany przez SS i Gestapo we wsi Połomia ( inne źródła podają , że było to we wsi Łubie ) .
Został wywieziony na wschód do koszar policyjnych w Bytomiu , gdzie podano mu jedzenie i wodę , a następnie do siedziby gestapo w Opolu , gdzie spędził noc .
Przez cały czas , jak zauważyli jego porywacze , był „ apatyczny , jego głowa była stale pochylona ” .
Następnego ranka porywacze zabrali go na posterunek policji w Gliwicach i umieścili w izolatce .
Krótko przed godziną 20 esesman w białym fartuchu , podający się za lekarza odwiedził Franciszka w jego celi i dał zastrzyk z silnym środkiem odurzającym .
Podczas prowokacji gliwickiej , gdy dowódca komanda Alfred Naujocks , zajmował się transmisją , żołnierze SS dostarczyli nieprzytomnego Honioka do budynku i położyli przy tylnych drzwiach .
W pewnym momencie – nie wiadomo kiedy – ktoś go zastrzelił .
Ciało Honioka , ubrane w polski mundur , pozostawiono na schodach wejściowych .
Do dziś nie jest znana przyczyna jego śmierci .
Przyczyną śmierci mogło być zarówno zastrzelenie , jak przedawkowanie .
SS-Sturmbannführera Alfred Naujocks
został poinformowany przez Heinricha Müllera , szefa Gestapo , że ma zostać dostarczony Polak , którego zakrwawione zwłoki mają zostać pozostawione w radiostacji jako niezbity „ dowód '' polskiej odpowiedzialności za atak .
Franciszek Honiok został wybrany , bo jego biografia pasowała do przedstawienia go opinii publicznej jako powstańca śląskiego .
