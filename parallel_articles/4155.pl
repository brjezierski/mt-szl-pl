Franciszek Ścigała
Franciszek Xawery Ścigała ( ur .
1 grudnia 1882 w Woźnikach , zm .
2 września 1940 w obozie w Gusen ) – polski duchowny katolicki , działacz społeczny i narodowy na Górnym Śląsku .
Życiorys .
Urodził się jako najstarszy syn listonosza Aleksandra i Pauliny z domu Skop .
W latach 1897–1906 uczył się w gimnazjum klasycznym w Królewskiej Hucie .
Ścigała był absolwentem Wydziału Teologicznego Uniwersytetu Wrocławskiego , gdzie związał się z polskim ruchem intelektualnym ( członek Kółka Polskiego w latach 1906–1907 ) .
W dniu 20 czerwca 1910 przyjął święcenia kapłańskie , a następnie skierowano go do pracy w Bogucicach , gdzie był wikarym w parafii św .
Szczepana .
W latach 1914–1917 został powołany do służby sanitarnej wojsk niemieckich w Goczałkowicach .
Następnie posługę duszpasterską pełnił w Siemianowicach Śląskich , w parafii Świętego Krzyża .
Był aktywny w trakcie powstań śląskich i plebiscytu .
Za swoją postawę w latach 1919–1921 został dwukrotnie odznaczony medalami powstańczymi ( Krzyżem na Śląskiej Wstędze Waleczności i Zasługi ) .
W 1923 został mianowany proboszczem parafii św .
