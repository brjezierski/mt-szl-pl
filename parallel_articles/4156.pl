Franck Ribéry
Franck Bilal Ribéry ( wym .
[ ] ; ur .
7 kwietnia 1983 w Boulogne-sur-Mer ) – francuski piłkarz występujący na pozycji pomocnika .
W latach 2006–2014 reprezentant Francji .
Zakończył karierę 22 października 2022 roku .
Kariera .
Ribéry zaczynał karierę w klubie US Boulogne , gdzie grał w sezonie 2001-2002 .
Następnym zespołem w karierze Francuza był Olympique Alès .
Po roku niespełna 20-letni zawodnik zmienił drużynę na sporo silniejszy Stade Brestois 29 .
Dobre występy w tym klubie zaowocowały podpisaniem kontraktu z pierwszoligowym FC Metz .
Tu także grał na wysokim poziomie , więc po rozegraniu 20 meczów Ribéry przeniósł się do tureckiego Galatasaray SK , gdzie zdobył swoje pierwsze trofeum – Puchar Turcji .
Jednak pod względem sportowym Ribéry nie był całkowicie zadowolony , toteż po pół roku wrócił do ojczyzny i grał w Olympique Marsylia .
W Marsylii Ribéry spisywał się znakomicie , co spowodowało , że stał się obiektem zainteresowania m.in .
