Poza tym Francuzi żyją głównie w swoich byłych koloniach w Afryce oraz w własnych terytoriach zamorskich w Oceanii i na innych kontynentach .
Około 10 milionów osób francuskiego pochodzenia mieszka w Stanach Zjednoczonych , a 5 milionów w Kanadzie .
