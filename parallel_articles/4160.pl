Większość Francuzów to katolicy ( chrystianizacja w II – IV wieku ) .
Dzisiaj wielu potomków byłych imigrantów uważa się za część narodu francuskiego , są to przeważnie osoby pochodzenia afrykańskiego i arabskiego .
Pozostają oni obywatelami państwa francuskiego .
Współcześnie przynależność do narodu francuskiego utożsamia się właśnie z obywatelstwem .
Faktycznie w prawie francuskim nie ma pojęcia obywatelstwa odrębnego od narodowości ( jest tylko narodowość , `` nationalité '' , która może być podwójna ) .
Wszelkie oficjalne statystyki dotyczące pochodzenia , rasy i religii obywateli są w tym kraju zabronione , gdyż w imię haseł rewolucji wszyscy Francuzi są równi .
W etnogenezie Francuzów najważniejszą rolę odgrywają zromanizowani Galowie ( tzw .
Galorzymianie ) oraz germańscy Frankowie , którzy zasymilowali się z ludnością romańską .
