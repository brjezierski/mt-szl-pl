Frankfurt nad Menem
Frankfurt nad Menem ( niem .
, , Frankfurt a. M. ) – miasto na prawach powiatu w Niemczech , w kraju związkowym Hesja , w rejencji Darmstadt , piąte pod względem liczby ludności miasto Niemiec ( ponad 760 tys .
mieszkańców pod koniec 2019 roku ) .
Natomiast cała metropolia ma populację ok. 5 milionów mieszkańców .
Jest to jedna z najważniejszych metropolii finansowych świata oraz znane centrum wystawiennicze .
Położenie .
Miasto leży po obu stronach rzeki Men u stóp gór Taunus .
Dzięki swojemu centralnemu położeniu stanowi ważny węzeł komunikacyjny , z drugim pod względem liczby pasażerów lotniskiem w Europie ( 8. miejsce na świecie ) .
Frankfurt jest największym miastem Hesji , lecz stolicą kraju związkowego jest Wiesbaden .
Frankfurt leży w obszarze 5,8 milionowego regionu metropolitalnego Ren-Men ( `` Rhein-Main-Gebiet '' ) .
