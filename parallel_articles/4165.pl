Nazwa .
Nazwa miasta wzmiankowana była po raz pierwszy w formie `` Vrankenvorde '' w 1253 roku .
Miasto otrzymało nazwę od Frankfurtu nad Menem .
Nazwa została przeniesiona znad Menu na nowo założone miasto na lewym brzegu Odry i oznaczała pierwotnie ‘ bród w kraju Franków ’ .
Osada leżąca po drugiej stronie Odry , czyli dzisiejsze Słubice , notowana była w średniowiecznych źródłach w formach `` Zbiwitz '' , `` Zbirwitz '' , `` Zliwiz '' .
Ze względu na zniekształcenie zapisów pierwotna forma jest trudna do ustalenia , a jej znaczenie jest niejasne .
