Freddy Krueger
Freddy Krueger ( pełne imię : Frederick Charles Krueger ) – fikcyjna postać , główny bohater serii horrorów `` Koszmar z ulicy Wiązów '' .
Psychopatyczny morderca dzieci .
Uwolniony , z braku dowodów przez sąd .
Zostaje zlinczowany ( spalony żywcem ) przez zdesperowanych mieszkańców miasteczka .
Po latach wraca : zakrada się do snów nastoletnich pociech swych zabójców – ale zabija naprawdę ; jedynie pokonując własne lęki można podjąć walkę z koszmarem .
Pisownia .
Pisownia imienia oraz – przede wszystkim – nazwiska sprawia problemy , dlatego bardzo często zapisuje się je błędnie .
Jedyną poprawną formą jest `` Freddy Krueger '' , jednak spotkać można się z dwoma anglojęzycznymi imionami wymawianymi jako `` fredi '' – Freddy i Freddie , których – niesłusznie – czasami używane są zamiennie .
W języku polskim pojawia się także `` Freddi '' .
Warto wspomnieć także o pisowni obu imion przez jedno „ d ” .
Dużym problemem jest , wyglądające na niemieckie , nazwisko Krueger – często spotkać można się z pisownią przez u z przegłosem ( Krüger ) , jednak jest ona niepoprawna ( nazwisko wymawia się jako `` kruger '' , nie `` kriger '' czy `` krüger '' , jak ma to miejsce w przypadku niemieckiego Krüger ) .
Występują także inne , również niepoprawne , formy nazwiska : Kruger ( bez „ e ” ) lub pisane przez „ c ” ( Crueger , Cruger , Crüger ) .
W Polsce powstała piosenka `` Fred Kruger '' zespołu Top One .
Historia serii filmów .
Cykl horrorów łączy motyw klasycznego kina grozy ( zemsta zza grobu ) z motywami współczesnymi ( krwawy psychopata ) .
Pierwszy film z serii pojawił się w 1984 roku , wyreżyserował go Wes Craven .
Scenariusz Cravena odrzuciło kilka wytwórni , dopiero New Line Cinema zgodziło się zrealizować film .
