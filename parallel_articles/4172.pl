Fredensborg
Fredensborg − miasto położone w północnej Zelandii w Danii nad jez .
Esrum , pomiędzy Hillerød i Elzynorem .
Do roku 2007 miasto było siedzibą władz gminy Fredensbog-Humlebæk , dziś należy do gminy Fredensborg położonej w regionie Region Stołeczny .
Historia .
Miasto powstało wraz z zamkiem ok. 1720 r. i początkowo było jedynie rezydencją podmiejską duńskiej rodziny królewskiej .
Zamek został nazwany na pamiątkę pokoju zawartego w 1722 r. i kończącego wielką wojnę północną ( duńska nazwa znaczy `` Zamek Pokoju '' ) .
Dopiero niedawno Fredensborg stał się miastem w pełnym tego słowa znaczeniu .
Uzyskał połączenie kolejowe z Hillerød i z Elzynorem .
Znany jest głównie z zamku królewskiego , który jest magnesem przyciągającym odwiedzających Danię turystów .
