Frederick Marryat
Komandor Frederick Marryat ( ur .
10 lipca 1792 w Londynie , zm .
9 sierpnia 1848 w Langham ) – angielski wojskowy , a także pisarz , autor nowel i powieści o tematyce marynistycznej , awanturniczej i gotyckiej , oraz książek dla dzieci .
Będąc oficerem Royal Navy usiłował przeciwstawiać się procederowi przymusowego poboru do marynarki wojennej , publikując broszurę `` Zalecenia służące zniesieniu obecnego systemu przymusowego wcielania do floty '' .
Swoje morskie doświadczenia wykorzystał w późniejszej twórczości .
Wzorujący się na opowiadaniach Charlesa Dickensa , dziś uważany za jednego z pionierów powieści marynistycznej .
Najbardziej znane w Polsce dzieła to `` Okręt widmo '' oraz `` Peter Simple '' .
