Frederiksberg ( stacja metra )
Frederiksberg – stacja metra w Kopenhadze , na linii M1 i linii M2 .
Zlokalizowana jest pomiędzy stacjami Fasanvej i Forum .
Została otwarta 12 października 2003 .
Po ukończeniu w 2017 roku nowej linii M3 stacja stanie się węzłem przesiadkowym zapewniając przesiadkę pomiędzy nową a istniejącą linią metra .
Historia .
Stacja Frederiksberg została otwarta 17 października 1864 , jako stacja pośrednia na głównej linii pomiędzy Kopenhagą i Roskilde .
