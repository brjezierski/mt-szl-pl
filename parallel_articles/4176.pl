Frederiksværk
Frederiksværk – miasto w Danii w gminie Halsnæs ( niegdyś Frederiksværk-Hundested ) , do roku 2007 siedziba gminy Frederiksværk .
Miasto leży nad fiordem Roskilde Fjord .
Według spisu ludności z 2018 roku miejscowość liczy 12 451 mieszkańców .
Wybudowany w 1719 roku Arresø Kanal łączący Roskilde Fjord z jeziorem Arresø umożliwił budowę we Frederiksværk pierwszych elektrowni wodnych i fabryk .
Miejscowość stała się pierwszym miastem przemysłowym w Danii .
Produkowano tu głównie broń dla duńskiej armii .
