Fredrikstad ( stacja kolejowa )
Fredrikstad – stacja kolejowa w Fredrikstad , w regionie Østfold w Norwegii , jest oddalona od Oslo Sentralstasjon o 94,26 km .
Leży na wysokości 7,5 m n.p.m .
Leży nieopodal lotniska Fredrikstad .
Ruch pasażerski .
Stacja obsługuje połączenia dalekobieżne między Oslo i Halden oraz międzynarodowe do Göteborga .
Pociągi w obie strony odjeżdżają co godzinę , a ze stacji wyjeżdża 5 międzynarodowych pociągów dzienne .
Obsługa pasażerów .
Wiata , poczekalnia , kasa biletowa , automat biletowy , parking , parking rowerowy , kawiarnia , ułatwienia dla niepełnosprawnych , schowki bagażowe , kiosk , przystanek autobusowy , postój taksówek .
