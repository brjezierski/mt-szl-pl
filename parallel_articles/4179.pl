Freeciv
Freeciv – strategiczna gra turowa wzorowana na serii `` Civilization '' Sida Meiera wydana na licencji GNU General Public License .
Projekt `` Freeciv '' zapoczątkowano w 1995 roku .
Wersja 1.0.0 została wydana w styczniu 1996 roku .
Serwis PC Gamer umieścił grę na liście 50 najlepszych darmowych gier na PC .
Gra jest dostępna na systemy operacyjne Microsoft Windows , AmigaOS i na systemy wykorzystujące X Window System ( systemy Unix , macOS , dystrybucje GNU/Linuksa ) .
Istnieją także porty gry na systemy mobilne – Maemo , Android .
W 2013 wprowadzono możliwość gry w przeglądarce internetowej obsługującej język HTML5 .
Zasady gry .
Podstawowym celem gry `` Freeciv '' jest budowa od podstaw , złożonego z wielu miast , własnego imperium .
Gracz podczas swojej misji styka się z innymi , sterowanymi przez komputer lub ludzi , graczami .
Dominację militarną i sukcesy w kampaniach wojskowych zapewnia rozwój armii , zaś gospodarczą i naukową handel oraz inwestowanie w odpowiednie budowle .
Warunkiem zwycięstwa jest budowa , wystrzelenie i zakończony sukcesem lot ( do gwiazdy Alfa Centauri ) statku kosmicznego , lub podbój militarny całego świata .
Gra rozpoczyna się w roku 4000 p.n.e .
i trwa domyślnie do roku 2000 , z możliwością zmiany na dowolny z zakresu od 0 do 5000 .
