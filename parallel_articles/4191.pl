Fryderyk II Wielki
Fryderyk II Wielki , niem .
`` Friedrich II von Hohenzollern '' ( ur .
24 stycznia 1712 w Berlinie , zm .
17 sierpnia 1786 w Poczdamie ) – król Prus w latach 1740–1786 .
Pod jego rządami Prusy stały się jednym z najpotężniejszych państw europejskich .
Dzieciństwo .
Ochrzczony został 31 stycznia 1713 w Berlinie .
Rodzicami chrzestnymi byli , m.in .
: cesarz Karol VI Habsburg oraz car Piotr I. Fryderyk decyzją swojego dziadka Fryderyka I otrzymał imiona Karol Fryderyk .
Imię Karol zostało nadane z myślą o cesarzu Karolu VI , jednakże nigdy nie było ono używane .
Fryderyk był najstarszym synem Zofii Doroty Hanowerskiej ( 1687-1757 ) , która w przeciwieństwie do swego męża była osobą wykształconą .
Fryderyk Wilhelm I , ojciec Fryderyka , gardził bowiem wszechstronną edukacją i blichtrem dworu , przedkładając nad nie proste męskie rozrywki : koszary i polowania .
Przedmiotem jego troski była przede wszystkim budowa potęgi militarnej Prus .
Wprowadził nawet zwyczaj noszenia munduru na co dzień ( stąd przezwisko `` Soldatenkönig '' : „ król żołnierzy ” , „ król-sierżant ” ) .
Fryderyk nie miał więc łatwego dzieciństwa , tym bardziej że szybko odkrył w sobie zamiłowanie do muzyki , starożytności , filozofii i literatury francuskiej , co zdecydowanie nie odpowiadało wyobrażeniom ojca o wychowaniu następcy tronu .
Pierwsze nauki pobierał pod opieką guwernantki , pani de Rocoules , francuskiej hugenotki .
Jej to zawdzięcza Fryderyk umiejętność posługiwania się językiem francuskim .
Największy wpływ na wychowanie miał Jacques-Egide Duhan de Jandun , który od 1716 r. objął urząd wychowawcy następcy tronu .
Można go uważać za ojca duchowego młodego Fryderyka .
M.in .
Duhan wbrew zakazowi królewskiemu uczył swego wychowanka łaciny .
Jednakże gdy ojciec dowiedział się o tym , nauka została przerwana , a młody Fryderyk , jak się wydaje , nigdy nie wybaczył tego ojcu .
Musiał z tego powodu czytać swoich ulubionych pisarzy antycznych i historyków rzymskich w przekładzie .
Popadł w ostry konflikt z ojcem , którego apogeum była próba ucieczki z kraju .
Ucieczka nie powiodła się : w nocy z 4 na 5 sierpnia 1730 grupę uciekinierów pochwycono i odstawiono do pruskiej twierdzy Wesel .
Wszczęto śledztwo – król postanowił bowiem potraktować uciekinierów jako dezerterów ( podejrzewając spisek , a może nawet próbę przewrotu z inicjatywy brytyjskiej ) .
Fryderyk został uwięziony w twierdzy w Kostrzynie nad Odrą – groziła mu nawet kara śmierci .
Ostatecznie sąd wojskowy nie ośmielił się wydać takiego wyroku .
Decyzją króla 6 listopada ścięto , w obecności Fryderyka , jego przyjaciela podejrzewanego o związek z księciem , Hansa Hermanna von Katte .
