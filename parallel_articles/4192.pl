Friedrich Bernhard Werner
Friedrich Bernhard Werner , właśc .
Friedrich Bernhard Wernher ( ur .
28 stycznia 1690 w Topoli koło Kamieńca Ząbkowickiego , zm .
20 kwietnia 1776 we Wrocławiu ) – śląski rysownik .
Życiorys .
Był synem Melchiora Wernera , bezrolnego chałupnika i jego żony Barbary Morawitz .
Rodzice nadali mu imiona na cześć Friedricha Steinera , zmarłego w 1681 r. opata kamienieckiego , i św .
Bernarda z Clairvaux , patrona zakonu cystersów .
Friedrich Bernhard kształcił się w kolegium jezuickim w Nysie razem z późniejszym opatem klasztorów w Kamieńcu Ząbkowickim i Lubiążu Tobiaszem Stusche .
Wkrótce porzucił naukę i zaciągnął się do wojska .
Nie wytrzymał jednak w armii zbyt długo i opuścił swój regiment , udając się na siedmioletnią wędrówkę po Europie .
W 1718 powrócił w rodzinne strony , gdzie 18 października tego roku ożenił się w Bardzie z córką ząbkowickiego mieszczanina , Marią Eleonorą Teresą Kirchpitter .
W latach 1726–1727 był kancelistą u biskupa wrocławskiego Franza Ludwiga von Neuburg .
Następnie na zlecenie augsburskich wydawców udał się w podróż po Europie , aby rysować wszystko , co wyda mu się godne uwiecznienia .
Rysunki te miały być podstawą sporządzenia miedziorytów .
Praca trwała do 1737 .
Powstał w ten sposób zbiór 341 wedut 219 różnych miast oraz 321 widoków kościołów , klasztorów , pałaców i innych budowli .
W 1733 otrzymał zamówienie od największego wydawcy map , firmy Hommans Erben z Norymbergi na wykonanie wedut , które miały być uzupełnieniem planowanego wydania atlasu Śląska .
Poczynając od 1735 , wyszło w Norymberdze kolejno 11 tablic miedziorytniczych , które zawierały 84 widoki Śląska pod wspólnym tytułem `` Scenographia Urbium Silesiae '' .
Około 1730 Werner napisał swoją autobiografię .
1 lipca 1736 w drodze do Augsburga przyjechał do Kamieńca Ząbkowickiego w celu przeprowadzenia podziału majątku po zmarłej teściowej .
Jego żona otrzymała wówczas niewielki majątek w Kamieńcu , a właściwie w jego części leżącej przy drodze do nieistniejącej dziś wsi Pilce , która była odrębną miejscowością do 1945 i nosiła nazwę Istebka .
W 1739 otrzymał od biskupa wrocławskiego , kardynała Philippa Sinzendorfa , posadę geometry dworskiego .
Po wybuchu wojny z Prusami biskup udał się na wygnanie , a Werner musiał poszukać sobie nowego zajęcia .
Próbował otrzymać pracę od króla Fryderyka II , ale pomimo zachęcających początków nic z tego nie wyszło .
W 1744 po raz ostatni była wzmiankowana jego żona , a 22 maja 1758 ożenił się powtórnie we Wrocławiu z Marią Magdaleną Treffa .
W 1746 r. rodzina rysownika sprzedała za 2275 talarów posiadłość w Istebce .
Ogród za 370 śląskich talarów otrzymał kuzyn Buchman .
Reszta gospodarstwa , które spłonęło po uderzeniu pioruna 9 lipca 1742 r. , nie była dużo warta , stąd spadek Wernera wyniósł tylko 800 talarów .
Wiadomo , że do 1766 zajmował on stanowisko inżyniera budowlanego w Izbie do Spraw Wojny i Dóbr Skarbowych Departamentu Budownictwa i Zabudowy Wiejskiej przy Królewskim Urzędzie Budowlanym we Wrocławiu .
Nie wiadomo , jaki był zakres jego obowiązków .
Nie są również znane rysunki , które powstały na zlecenie tego urzędu lub kardynała Sinzendorfa .
Wiadomo natomiast , że od tego czasu Werner poświęcił się wyłącznie rysowaniu krajobrazów Śląska .
Wynikiem tego jest licząca 3000 stron 5-tomowa `` Topographia Silesiae '' oraz licząca 1400 rycin topografia hrabstwa kłodzkiego .
Dzieło `` Topographia Silesiae '' powtórzył jeszcze trzy razy , przy czym za każdym razem dodawał nowe ryciny .
Próbował swoich sił także w miedziorytnictwie .
W latach 1748–1758 opublikował księgę domów modlitwy z 164 ilustracjami , a zaraz potem serię widoków klasztorów cysterskich Czech , Moraw i Śląska .
`` Topografia '' hrabstwa kłodzkiego zaginęła podczas II wojny światowej , a z czterech egzemplarzy `` Topographia Silesiae '' pozostały liczne fragmenty , wśród których brak jedynie księstwa jaworskiego .
Pod koniec życia Werner znalazł się w trudnej sytuacji materialnej .
Skłoniło go to m.in .
do sprzedania swojego pamiętnika , który zawierał również panoramy śląskie i europejskie .
Zmarł bezdzietnie i w zapomnieniu we Wrocławiu .
Rysunki Friedricha Bernharda Wernera nie są cennymi dziełami artystycznymi .
Posiadają one jednak ogromne znaczenie z uwagi na fakt , iż przedstawiają wiele budowli , które już nie istnieją lub zostały przebudowane i dlatego są bardzo ważnym źródłem dla historyków sztuki i konserwatorów zabytków .
