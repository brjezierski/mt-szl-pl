Friedrich Grundmann
Friedrich Wilhelm Grundmann ( ur .
26 listopada 1804 w Berthelsdorf − obecnie Barcinek koło Jeleniej Góry − podówczas Saksonia , zm .
30 lipca 1887 w Katowicach ) − niemiecki przedsiębiorca .
Życie .
Kiedy miał 7 lat , z ojcem przeniósł się do Tarnowskich Gór , gdzie ukończył ewangelicką szkołę podstawową .
W tarnogórskiej szkole górniczej , do której uczęszczał , poznał Franciszka Wincklera .
Pracował w kopalniach `` Królowa Luiza '' w Zabrzu i `` Król '' w Królewskiej Hucie ( obecnie Chorzów ) .
Był sztygarem w tarnogórskiej `` Friedrichsgrube '' i nauczycielem w szkole górniczej .
Po śmierci Franza Wincklera w 1851 r. został zarządcą dóbr rodziny Wincklerów .
Dokonał w nich gruntownych przemysłowych przemian , wraz z budowniczym Heinrichem M. A. Nottebohmem był autorem koncepcji rozbudowy miejskiej Katowic .
Skutecznie zabiegał o nadanie Katowicom praw miejskich , co nastąpiło w 11 września 1865 r. W 1857 roku w Opolu założył cementownię `` Portland Zementwerke '' .
Na rzecz Katowic pracował do 1872 r. , kiedy to przeszedł na emeryturę .
Honorowy obywatel Miasta Katowice .
Jego nazwisko nosiła do I wojny światowej dzisiejsza ul .
3 Maja w centrum Katowic , a obecnie odcinek nowo wybudowanej obwodnicy śródmiejskiej pomiędzy ulicami Gliwicką a Chorzowską .
Willa Grundmanna .
Grundmann przez niektórych przedstawicieli władz politycznych Katowic w okresie PRL-u nie był uważany za postać pozytywną .
Jego okazałą willę usytuowaną na rogu ulicy Warszawskiej i ulicy Bankowej , będącą zabytkiem architektonicznym , służącą jeszcze po roku 1945 jako siedziba Urzędu Stanu Cywilnego , rozebrano do fundamentów .
Budynek upamiętnia niewielka szklana plakieta na ścianie stojącego w tym miejscu nowego budynku Narodowego Banku Polskiego .
Być może nie bez znaczenia był też fakt , że w budynku tym mieszkał w okresie międzywojennym Józef Rymer , zatrzymał się w nim marszałek Józef Piłsudski a później mieściła się tu katowicka Kuria Biskupia i przez krótki czas mieszkał późniejszy prymas kard .
August Hlond .
