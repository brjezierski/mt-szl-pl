Fritz Laband
Fritz Laband ( ur .
1 listopada 1925 w Zabrzu na Górnym Śląsku , zm .
3 stycznia 1982 w Hamburgu ) , niemiecki piłkarz , obrońca i pomocnik .
Mistrz świata z roku 1954 .
W reprezentacji RFN debiutował 25 kwietnia 1954 w meczu ze Szwajcarią .
Rozegrał w kadrze łącznie tylko 4 spotkania , ale trzy z nich miały miejsce podczas MŚ 54 , co dało mu tytuł mistrza świata .
W czasie turnieju był piłkarzem Hamburger SV .
Wcześniej grał w Reichsbahn SV Hindenburg ( Zabrze ) ( 1936-37 ) , Hindenburg 09 ( Zabrze ) ( 1938-43 ) , SC Preußen Zaborze ( Zabrze ) ( 1943 ) i Anker Wismar ( 1946-49 ) , z którym zdobył w 1949 mistrzostwo Meklemburgii .
Po wojnie osiedlił się w środkowych Niemczech , w sowieckiej strefie okupacyjnej .
Podczas uczestnictwa w meczu reprezentacji NRD przeciwko reprezentacji Czechosłowacji w 1950 zwrócił na siebie uwagę obserwatorów klubu HSV , którzy przekonali go do ucieczki do Hamburga .
W latach 1949–1955 w `` HSV '' rozegrał 143 mecze , w sezonie 56/57 był zawodnikiem Werderu Brema , a następnie Grün-Weiss 07 Hamburg ( 1958 ) .
Po zakończeniu aktywnej kariery trenował : Buxtehuder SV von 1892 e.V. , Uhlenhorster Sport Club Paloma Hamburg von 1909 e.V .
oraz Lurup Hamburg 1923 e.V ..
W latach późniejszych , jako gastronom popadł w poważne tarapaty finansowe i musiał zacząć pracować w swoim wyuczonym zawodzie elektryka , a krótko przed śmiercią jako kierowca szefa firmy `` Kobolt '' w Hamburgu .
Na początku lat 80. stwierdzono u niego nowotwór krtani ( był nałogowym palaczem tytoniu ) .
W wieku 56 lat zmarł na raka w szpitalu św .
Jerzego w Hamburgu .
