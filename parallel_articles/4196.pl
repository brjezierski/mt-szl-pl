Friedrich Schlegel
Karl Wilhelm Friedrich von Schlegel ( ur .
10 marca 1772 , zm .
12 stycznia 1829 ) – niemiecki poeta , krytyk literacki , językoznawca i filozof .
Życiorys .
Jeden z twórców współczesnego językoznawstwa .
Interesował się też literaturoznawstwem .
Brat Augusta Wilhelma Schlegela .
Razem z Novalisem , Augustem Wilhelmem Schlegelem , Friedrichem Wilhelmem Josephem Schellingiem uchodzi za najwybitniejszego przedstawiciela romantyzmu jenajskiego .
Jest uznawany za twórcę pojęcia ironii romantycznej .
Pisał teksty o tematyce społeczno obyczajowej lub historycznej .
W swej pracy `` Über die Sprache und Weisheit der Indier '' ( O języku i wiedzy Indusów ) wydanej w Heidelbergu , w 1808 , jako pierwszy wyraźnie podkreślił związki zachodzące między greką , łaciną , językami germańskimi i sanskrytem , dzięki czemu dał początek indoeuropeistyce .
