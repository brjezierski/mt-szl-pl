Frigyes Karinthy
Frigyes Karinthy ( pełne nazwisko Frigyes Ernő Karinthy , ur .
25 czerwca 1887 w Budapeszcie , zm .
29 sierpnia 1938 w Siófoku ) – węgierski pisarz , poeta i tłumacz .
Ojciec Ferenca Karinthiego .
Życiorys .
Jego ojciec , József Karinthi ( 1846–1921 ) , był wykształconym urzędnikiem , jednym z założycieli Węgierskiego Towarzystwa Filozoficznego ( Magyar Filozófiai Társaság ) .
W 1874 roku zmienił nazwisko z Kohn na Karinthi .
Wraz z żoną oraz czterema córkami przeszedł na religię ewangelicką 3 stycznia 1886 roku .
Dwóch ich synów urodziło się już jako chrześcijanie .
Po śmierci żony Karoliny Engel ( 1850–1895 ) Karinthi sam wychowywał swoich sześcioro dzieci .
Frigyes Karinthy zaczął pisać w latach 1898–1900 .
