Fritz Zulauf
Fritz Zulauf ( ur .
19 kwietnia 1893 , zm .
w grudniu 1941 w Zurychu ) – szwajcarski strzelec , dwukrotny medalista olimpijski , multimedalista mistrzostw świata .
Zulauf wystąpił w trzech konkurencjach na igrzyskach olimpijskich w Antwerpii w 1920 roku .
Zdobył dwa brązowe medale , oba w strzelaniu z pistoletu wojskowego z 30 m ( zarówno indywidualnie , jak i drużynowo ) .
Zulauf dziesięciokrotnie zdobywał medale mistrzostw świata .
