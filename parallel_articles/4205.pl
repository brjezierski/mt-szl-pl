Jesionik
Jesionik ( , do 1947 `` Frývaldov '' , , pol .
dawniej `` Frywałdów '' , `` Frywałd '' ) – miasto w Czechach , w kraju ołomunieckim , leżące w Górach Opawskich ( cz .
`` Zlatohorská vrchovina '' ) , na pograniczu Wysokiego Jesionika ( cz .
`` Hrubý Jeseník '' ) i Gór Złotych ( cz .
`` Rychlebské hory '' ) .
Jesionik położony jest na Czeskim Śląsku , a historycznie na Dolnym Śląsku .
Według danych z 1 stycznia 2015 powierzchnia miasta wynosiła 3823 ha , a liczba jego mieszkańców 11 524 osób .
Historia .
Pierwsza wzmianka o Jesioniku pochodzi z 1267 , z czasów kolonizacji kraju .
Już z tego okresu pochodzą informacje o poszukiwaniach górniczych rud metali .
W źródłach łacińskich miasto figuruje jako `` Vriwald '' , w niemieckich jako `` Freiwalda '' .
W przeszłości Jesionik był największym miastem biskupiego księstwa nyskiego , leżącym na obecnych terenach czeskich .
W czasach późniejszych rozwój miasta i okolicy związany był przede wszystkim z pierwszym na świecie zakładem wodoleczniczym , który został założony przez Vincenza Priessnitza .
Stąd pochodzi czeska nazwa uzdrowiska – Priessnitzovy lázně ( oficjalnie Lázně Jeseník , niem .
Bad Gräfenberg ) , a główny pawilon nosi nazwę Priessnitzovo sanatorium .
Nazwa drugiego sanatorium ( pawilonu ) Ripper pochodzi od nazwiska zięcia Priessnitza , który miał duży wpływ na rozwój uzdrowiska .
Pawilon dziecięcy – Karolina nosi imię córeczki Priessnitza , której nie udało się wyleczyć i zmarła , kiedy miała 3 lata .
Najdłuższy pawilon nazywa się Bezruč , a kolejny – Maryčka .
W 1888 miasto uzyskało połączenie kolejowe z Hanušovicami do Głuchołaz – budowa wymagała skomplikowanych rozwiązań technicznych ze względu na duże różnice wysokości na trasie biegnącej przez Przełęcz Ramzovską ( cz .
`` Ramzovské sedlo '' ) ( początkowo planowano tunel pod Červenohorskim sedlem , ale zarzucono go z powodów finansowych ) .
