Fröhnd
Fröhnd – gmina w Niemczech , w powiecie Lörrach , w rejencji Fryburg , w kraju związkowym Badenia-Wirtembergia .
Wchodzi w skład związku gmin Schönau im Schwarzwald .
Leży w regionie Hochrhein-Bodensee , w Schwarzwaldzie , na północ od Schönau im Schwarzwald .
W skład gminy wchodzą miejscowości Holz , Hof , Ittenschwand , Kastel , Künaberg , Niederhepschingen , Oberhepschingen , Stutz , Unterkastel .
