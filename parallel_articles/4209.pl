`` Castrum Dominae Nostrae '' , „ Zamek Naszej Pani ” , `` Civitas Warmiensis '' , „ Miasto Warmińskie ” ) – miasto na Warmii w woj .
warmińsko-mazurskim , w powiecie braniewskim , siedziba gminy miejsko-wiejskiej Frombork .
Jest położony na Równinie Warmińskiej , nad Zalewem Wiślanym , na północnym krańcu Wysoczyzny Elbląskiej .
W miejscowości znajduje się port morski oraz morskie przejście graniczne .
Frombork leży zarówno na historycznej Warmii biskupiej , jak i na obszarze dawnej Warmii plemiennej .
Uzyskał prawa miejskie w 1310 .
Utracił je po II wojnie światowej w 1945 , aby ponownie uzyskać je w 1959 .
W latach 1954–1957 wieś Frombork należała i była siedzibą władz gromady Frombork do 1972 r. W 1958 r. wieś Frombork wyłączono z gromady , tworząc osiedle miejskie Frombork , któremu w 1959 r. nadano prawa miejskie .
Po reformie administracji w 1973 utworzono gminę miejsko-wiejską Frombork .
