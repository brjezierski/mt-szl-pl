Fuenterrabía
Fuenterrabía ( w jęz .
baskijskim Hondarribia ) – miasteczko położone na zachodnim brzegu rzeki Bidasoa , w Guipúzcoa , w Kraju Basków .
Miasto zachowało średniowieczny rynek , mury ( XV wiek ) i zamek ( X wiek ) .
Zabytkowe centrum miasta skupione jest wokół barokowego kościoła Santa Maria de la Asunción .
Na zewnątrz kościoła warto zwrócić uwagę na masywne przypory i barokową wieżę , wewnątrz natomiast na złocone retabulum .
Przy mieście znajduje się lotnisko miasta San Sebastián , które obsługuje loty krajowe w Hiszpanii .
Liczba mieszkańców w 2005 : 15,7 tys .
Miasto było w przeszłości ważną twierdzą , z którą wiążą się bitwy rozegrane w latach 1521 , 1638 i 1792 .
