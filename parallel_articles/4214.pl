Fugazi ( album )
Fugazi – drugi album zespołu Marillion .
W roku 1998 została wydana wersja 2CD remaster .
W tym albumie po raz pierwszy pojawia się Ian Mosley , perkusista .
Utwór `` Fugazi '' w 2020 roku w 26. notowaniu Topu Wszech Czasów Programu Trzeciego Polskiego Radia zajął 85. miejsce .
Od tytułu albumu i utworu zaczerpnięto nazwę warszawskiego klubu Fugazi .
