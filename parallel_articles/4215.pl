Fudżi
– czynny stratowulkan i zarazem najwyższy szczyt Japonii ( 3776 m n.p.m . ) .
Leży na wyspie Honsiu ( `` Honshū '' ) , na południowy zachód od stolicy , Tokio .
W 2013 roku góra Fudżi została wpisana na listę światowego dziedzictwa UNESCO jako obiekt dziedzictwa kulturowego – święte miejsce i źródło artystycznej inspiracji .
Wyjaśnienie nazwy góry .
Wokół nazwy góry Fudżi powstało m.in .
nieporozumienie wynikające z dwoistości czytania znaku ( kanji ) 山 ( oznaczającego „ górę ” ) .
Jego sinojapońskie odczytywanie brzmi `` san '' ( od chińskiego `` shān '' ) .
Natomiast zgodnie z odczytywaniem japońskim znak ten czyta się `` yama '' – stąd alternatywna nazwa góry : `` Fuji-no-yama '' 富士の山 .
Po chińsku nazwa góry jest zapisywana identycznie , a czytanie to : `` Fùshìshān '' .
Wskutek zbieżności fonetycznej słowo `` san '' określające „ górę ” utożsamia się często z sufiksem honoryfikatywnym `` -san '' , występującym po nazwiskach japońskich tytułem grzecznościowym , oznaczającym „ pan/pani ” .
Stąd też czasem tworzone jest błędne sformułowanie „ szanowna Fudżi ” , czy też „ pan/pani Fudżi ” .
