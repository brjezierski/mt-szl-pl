Historia .
Miasto powstało w odległej przeszłości jako miasto świątynne zbudowane wokół świątyni buddyjskiej Yugyō-ji ( inaczej : Shōjōkō-ji , formalnie : Tōtaku-san Muryōkō-in Shōjōkō-ji ) , a w nowszej historii jako punkt postojowy ( shukuba ) przy trakcie Tōkaidō , głównym szlaku łączącym Kioto z Edo ( ob .
Tokio ) .
Po restauracji Meiji obszar zajmowany przez obecne miasto był kilkukrotnie dzielony i łączony administracyjnie .
Zaczął się szybko się rozwijać po otwarciu stacji kolejowej należącej do głównej linii Tōkaidō w 1887 roku i reformie katastralnej w 1889 roku .
Po wchłonięciu dalszych okolicznych wiosek w 1908 roku powstała nazwa Fujisawa .
Status miasta na poziomie 市 `` shi '' ( ponad 50 tys .
mieszkańców ) otrzymała 1 października 1940 roku .
Enoshima .
Szczególnym zainteresowaniem cieszy się położona blisko brzegu i połączona z lądem mostem wyspa Enoshima ( E-no-shima ) , na której znajduje się chram poświęcony bogini Benzaiten ( Benten ) .
Jest ona jedyną kobietą spośród siedmiorga japońskich bogów szczęścia ( shichi-fukujin ) .
Wyspa Enoshima jest znana nie tylko jako miejsce kultu i atrakcja turystyczna , ale także jako przystań żeglarska .
Była ona miejscem rozgrywania regat w ramach Letnich Igrzysk Olimpijskich 1964 i Letnich Igrzysk Olimpijskich 2020 , zorganizowanych w 2021 roku w wyniku opóźnienia spowodowanego pandemią COVID-19 .
