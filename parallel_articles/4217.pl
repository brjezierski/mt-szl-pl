Fujisawa
– miasto w Japonii , w prefekturze Kanagawa , na wyspie Honsiu ( `` Honshū '' ) .
Położenie .
Miasto Fujisawa położone jest w centralnej części prefektury Kanagawa , nad zatoką Sagami , do której wpadają ciepłe wody prądu morskiego Kuro-shio .
W północnej części miasta znajduje się płaskowyż Sagami , który powstał w wyniku aktywności strefy wulkanicznej Fudżi około 400 tys .
lat temu .
Na południu leżą aluwialne niziny wydm Shōnan .
Rzeki Sakai i Hikiji płyną z północy na południe , ich wody wpadają do zatoki Sagami .
Miasto leży w centrum prefektury Kanagawa nad zatoką Sagami .
