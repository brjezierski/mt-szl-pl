Fusarium oxysporum
Fusarium oxysporum Schltdl – gatunek grzybów z rodziny gruzełkowatych ( `` Nectriaceae '' ) .
Pasożyt wielu roślin , wywołujący u nich chorobę zwaną fuzariozą .
Jest najważniejszym gospodarczo gatunkiem w obrębie rodzaju `` Fusarium '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Fusarium , Nectriaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Ma ponad 160 synonimów .
Większość wśród nich to odmiany i formy , przez Index Fungorum jednak uznane za synonimy .
Morfologia .
Grzyb mikroskopijny .
Morfologicznie i funkcjonalnie jest bardzo zmienny i tworzy liczne szczepy .
Watowata grzybnia rozwija się bardzo szybko .
Początkowo jest biała , potem brzoskwiniowa lub fioletowa .
Wytwarza dwa rodzaje konidiów .
Mikrokonidia zazwyczaj bardzo liczne , powstają na zakończeniach bocznych strzępek , lub na prostych , lub słabo rozgałęzionych konidioforach .
Mają dość zmienne kształty – owalne , elipsoidalne lub wałeczkowate , są proste lub zakrzywione , o rozmiarach 5–12 × 2,2–3,5 μm .
Makrokonidia rzadkie , powstające na bardziej rozgałęzionych konidioforach , lub na powierzchni grzybni w tworach podobnych do sporodochiów .
Są cienkościenne , wrzecionowato-szydłowate , wyjątkowo wrzecionowato-sierpowate .
Mają 3–7 przegród ( najczęściej 3–5 ) i różne rozmiary :
Chlamydospory zazwyczaj liczne , o ścianach gładkich lub chropowatych .
Powstają zazwyczaj pojedynczo , ale czasami w parach lub w łańcuszkach .
Występowanie i siedlisko .
`` Fusarium oxysporum '' występuje w glebie na całym świecie i jest w niej jednym z najbardziej rozprzestrzenionych mikroorganizmów .
Znaleziono go w piaskach pustyni Sonora , w glebie tropikalnych i umiarkowanych lasów , łąk i w glebach tundry .
Zakres żywicieli tego gatunku jest bardzo szeroki i obejmuje zarówno rośliny , jak i zwierzęta .
Jako pasożyt może żyć kosztem bardzo licznych gatunków roślin nagonasiennych , okrytonasiennych i szerokiej gamy zwierząt , od stawonogów począwszy , po człowieka .
Szczepy żyjące na roślinach są polifagiczne , natomiast te żyjące na zwierzętach i ludziach są monofagiczne , wykazują dużą specjalizację .
Zakażenie roślin następuje w glebie .
`` Fusarium oxysporum '' może w niej przetrwać bardzo długo .
