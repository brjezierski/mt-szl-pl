Fusarium poae
Fusarium poae ( Peck ) Wollenw .
– gatunek grzybów z rodziny gruzełkowatych ( `` Nectriaceae '' ) .
Mikroskopijny grzyb pasożytniczy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Fusarium , Nectriaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1904 r. Charles Horton Peck , nadając mu nazwę `` Sporotrichum poae '' .
Obecną nazwę nadał mu Hans Wilhelm Wollenweber w 1914 r .
Synonimy :
Morfologia .
Kultury na podłożach hodowlanych wykazują szybki wzrost w porównaniu z innymi gatunkami `` Fusarium '' .
Po 4 dniach hodowli w ciemności w 25 °C osiągają średnicę 65–73 mm .
