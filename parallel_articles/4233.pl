Piłkarz
Piłkarz ( w odniesieniu do kobiet stosuje się formę piłkarka ) – osoba uprawiająca piłkę nożną .
Określenie stosowane mniej precyzyjnie w przypadku sportowca uprawiającego futsal ( piłkarz halowy , futsalista ) , piłkę nożną plażową ( piłkarz plażowy ) , ręczną ( piłkarz ręczny , szczypiornista ) czy wodną ( piłkarz wodny , waterpolista ) .
