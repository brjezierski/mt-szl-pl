Podział ze względu na pasy działań .
Boisko można podzielić ze względu na pasy działań .
Wyróżnia się trzy pasy działań : `` lewy boczny '' , `` środkowy '' i `` prawy boczny '' .
Podział ten wykorzystują niektórzy trenerzy koszykówki , by nauczyć młodych koszykarzy , że aby podanie było bezpieczne , nie należy podawać przez wszystkie pasy , ale tylko w obrębie dwóch , z jednego do drugiego , dlatego np .
chcąc bezpiecznie przerzucić piłkę z pasa lewego bocznego do prawego bocznego , należy skorzystać z pomocy zawodnika znajdującego się w pasie środkowym .
Podział ten jest też wykorzystywany dla zobrazowania rozmieszczenia zawodników na boisku .
W większości taktyk po dwóch koszykarzy znajduje się na pasach bocznych , a jeden w środku .
Podział ze względu na „ wysokość ” .
Boisko można podzielić ze względu na tzw .
„ wysokość ” .
Wyznacza się „ górę ” i „ dół ” boiska , a konkretnie jego jednej połowy , na której toczy się gra .
