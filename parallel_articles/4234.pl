Boisko ( koszykówka )
Boisko – w koszykówce według zasad FIBA twarda , płaska powierzchnia o długości 28 metrów i szerokości 15 metrów , bez przeszkód .
Boisko to przestrzeń na której grają zawodnicy , ograniczona liniami autu .
Do boiska zalicza się także kosz .
Linie boiska powinny mieć biały kolor i posiadać szerokość 5 cm .
