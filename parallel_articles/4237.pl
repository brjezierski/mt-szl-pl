Skarpetki
Skarpety , skarpetki – element codziennego ubioru , zakładany na stopy .
Zastąpiły powszechnie niegdyś używane onuce .
Skarpetki są noszone zarówno przez mężczyzn , jak i kobiety w wielu kręgach kulturowych świata .
W minimalistycznej wersji mają one kształt ludzkiej stopy , a przeważnie sięgają za kostki .
Materiał , z jakiego są wykonane , zależy między innymi od przeznaczenia skarpet .
Skarpetki są warstwą ubioru , chroniącą stopy przed zimnem ( zarówno izolując stopy od chłodu z zewnątrz , jak i odprowadzając pot na zewnątrz ) , otarciami oraz izolującą je od obuwia .
