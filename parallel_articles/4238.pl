Fuzuli
Mehmed ibn Süleyman Fuzuli ( ur .
ok. 1495 , zm .
1556 ) – azerski poeta tworzący w językach perskim , azerskim i arabskim .
Życiorys .
Fuzuli urodził się na terenie obecnego Iraku .
Jego rodzina należała do oguzkiego plemienia Bajat .
Jednak mimo koczowniczego pochodzenia , rodzina Fuzûlî od dawna była osiedlona w miastach .
Początkowo Fuzuli był nauczany przez swojego ojca , muftiego w Al-Hilla , następnie przez nauczyciela o imieniu Rahmetullah .
W tym czasie poznał języki turecki i arabski .
Jego ojczystym językiem był język azerski .
W 1534 osmański sułtan Sulejman I Wspaniały podbił Bagdad , w którym mieszkał Fuzuli .
Poeta uzyskał wówczas możliwość zostania nadwornym poetą .
Uzyskał wówczas stypendium , które jednak z powodu biurokracji nigdy nie zostało mu wypłacone .
Fuzuli zmarł w 1556 podczas epidemii dżumy .
Twórczość .
Fuzûlî stworzył trzy dywany w językach perskim , arabskim i azerskim , jak również wiele poematów .
Jednym z najsłynniejszych jego poematów jest `` Haszysz i Wino '' , w którym porównuje on sułtana Bajazyda II do haszyszu a szacha Isma'ila I do wina .
Równie znany jest poemat `` Lajla i Madżnun '' , którego tematem jest klasyczna bliskowschodnia historia miłosna Lajli i Madżnuna .
Fuzûlî uważany jest za jednego z najwybitniejszych poetów tureckich .
Jego twórczość miała ogromny wpływ na literaturę zarówno Turcji jak i Azerbejdżanu .
Poemat `` Beng ü Bâde '' został przetłumaczony na język polski przez Stanisławę Płaskowicką-Rymkiewicz pt .
`` Haszysz i Wino '' i wydany w 1973 roku przez Ossolineum .
