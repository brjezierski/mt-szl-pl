Førde ( miasto )
Førde – ośrodek administracyjny gminy Førde w zachodniej Norwegii , w okręgu Sogn og Fjordane .
Miasto położone jest u ujścia rzek Anga i Jølstra , na wschodnim krańcu fiordu Førdefjorden , około 79 km na południowy wschód od ośrodka administracyjnego gminy Bremanger - Svelgen .
W niedalekiej odległości od miasta leżą jeziora : Movatnet , Bekkjevatnet , Langelandsvatnet .
Miasto Førde jest ważnym centrum handlowym , przemysłowym i rządowym w tym regionie .
Region Øyrane wzdłuż portu jest przemysłową częścią miasta .
Ponadto w Førde znajduje się jedna z dwóch filii uczelni Sogn og Fjordane University College - Wydział Inżynierii i Medycyny .
W Førde znajduje się kościół , który wybudowany został w 1885 roku .
