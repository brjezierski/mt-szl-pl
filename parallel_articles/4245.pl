GKS Katowice ( piłka nożna kobiet )
GKS Katowice – sekcja piłki nożnej kobiet klubu sportowego GKS Katowice .
Drużyna powstała w 2015 roku , w roku 2018 awansowała do Ekstraligi .
Drużyna powstała latem 2015 roku .
Zespół utworzyły zawodniczki , które przeszły ze zlikwidowanego w tym samym czasie spadkowicza z Ekstraligi , klubu 1 .
FC AZS AWF Katowice .
Drużyna rozpoczęła grę w rozgrywkach piłkarskich od III ligi ( czwarty poziom rozgrywkowy ) w sezonie 2015/2016 , w każdym sezonie notując awans , aż w roku 2018 zespół zadebiutował w Ekstralidze .
Początkowo piłkarki GKS-u swoje spotkania rozgrywały na stadionie Kolejarza Katowice w dzielnicy Piotrowice , po awansie do Ekstraligi przeniosły się na stadion Podlesianki w Katowicach-Podlesiu .
