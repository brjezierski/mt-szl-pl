GKS Tychy ( koszykówka )
GKS Tychy – polski klub koszykarski z siedzibą w Tychach , występujący w I lidze .
Założony 20 kwietnia 1971 .
W 2009 nazwę klubu zmieniono z KKS Big Star Tychy na KKS Tychy , zaś w 2010 na GKS Tychy .
W sierpniu 2011 klub ogłosił bankructwo .
W sezonie 2011/12 GKS Tychy wystartował w rozgrywkach III ligi , grupy śląskiej II .
W tym także sezonie drużyna awansowała do II ligi .
Po sezonie 2013/2014 zakończonym awansem wróciła do I ligi .
