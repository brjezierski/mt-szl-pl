Gabardyna
Gabardyna – nazwa średnio grubej , mocno utkanej tkaniny o ukośnym splocie .
Gabardyna używana jest w produkcji płaszczy , garsonek , garniturów , spodni kostiumów i innych części garderoby a przede wszystkim mundurów galowych .
Włókno używane do wyrobu tej tkaniny jest tradycyjnie wełną , lecz może to też być włókno bawełniane , syntetyczne , bądź mieszanka wymienionych włókien .
Materiał ten został wynaleziony pod koniec XIX wieku przez Thomasa Burberry'ego ( 1835–1926 ) .
Odzież z tej tkaniny była noszona przez polarników ( np .
przez Roalda Amundsena podczas zdobywania bieguna południowego w 1911 oraz Ernesta Shackletona podczas wyprawy transantarktycznej 1914–1916 ) i himalaistów ( np .
przez George ' a Mallory'ego podczas próby zdobycia Mount Everestu w 1924 ) .
