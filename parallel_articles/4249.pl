Gaborone
Gaborone – stolica i największe miasto Botswany , ośrodek administracyjny Dystryktu South East .
W 2011 roku liczba mieszkańców wynosiła 227 333 .
Geografia .
Gaborone to stolica i największe miasto Botswany .
Miasto znajduje się południowo-wschodniej części kraju .
Znajduje się około 15 km od granicy z RPA .
Gaborone zostało wybrane na stolicę ze względu na strategiczne położenie , bliskość linii kolejowej oraz Pretorii .
