Gabriel Jesus
Gabriel Jesus , właśc .
Gabriel Fernando de Jesus ( ur .
3 kwietnia 1997 w São Paulo ) – brazylijski piłkarz , występujący na pozycji napastnika w angielskim klubie Arsenal oraz w reprezentacji Brazylii .
Złoty medalista Letnich Igrzysk Olimpijskich 2016 .
Kariera klubowa .
Początki .
Urodzony w São Paulo , Gabriel Jesus dorastał w okolicy Jardim Peri .
Po początkowym graniu w piłkę nożną na brazylijskich ulicach , dołączył do amatorskich klubów w regionie , a ostatnim zespołem w którym grał przed rozpoczęciem kariery zawodowej był Associação Atlética Anhanguera .
Palmeiras .
1 lipca 2013 roku Jesus podpisał umowę z młodzieżową drużyną SE Palmeiras .
Został najlepszym strzelcem klubu w tym sezonie , strzelając 54 gole w 48 meczach .
W styczniu 2014 po długich negocjacjach , Jesus podpisał trzyletni kontrakt z Palmeiras .
Strzelił 37 bramek w 22 meczach dla Palmeiras w 2014 roku w drużynie do lat 17 , co zainteresowało kilka większych drużyn i wpłynęło sporo ofert z klubów w całym kraju , jak i zagranicą .
Został po raz pierwszy włączony do składu Palmeiras na starcie sezonu w dniu 27 sierpnia 2014 roku .
Całe spotkanie przesiedział na ławce rezerwowych , a jego drużyna przegrała 0-1 u siebie z Atlético Mineiro .
Podczas bieżącego sezonu nie rozegrał ani jednego meczu w dorosłej drużynie .
Jesus zadebiutował w seniorskiej piłce w dniu 7 marca 2015 roku przeciwko Campeonato Paulista , zastępując Leandro Pereirę w 73. minucie .
