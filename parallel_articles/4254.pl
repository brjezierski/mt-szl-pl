Nietoperze ( skała )
Nietoperze – skały na Wyżynie Częstochowskiej , we wsi Piaseczno w województwie śląskim .
Znajdują się na szczycie niewielkiego wzniesienia o wysokości 403 m n.p.m .
Dawniej znajdowały się na terenie otwartym , teraz jednak zarastającym chaszczami .
Zbudowane są z wapienia , mają wysokość 10–12 m , ściany połogie , pionowe lub przewieszone z kominami , filarami i zacięciami .
Uprawiana jest na nich wspinaczka skalna .
Obok skał biegnie czerwono znakowany Szlak Maryjny .
Drogi wspinaczkowe .
Na Nietoperzach wspinano się w latach 80. i 90 .
XX wieku .
Obecne są mało popularne .
Są na nich 22 drogi wspinaczkowe o trudnościach od IV do VI.2+ w skali polskiej .
Część z nich posiada asekurację : ringi ( r ) i stanowiska zjazdowe ( st ) .
Ściany wspinaczkowe o wystawie północno-wschodniej , wschodniej , południowo-wschodniej , południowej , południowo-zachodniej , w 2022 r. dojścia do nich zarośnięte chaszczami dochodzącymi pod same skały .
