Gaeumannomyces graminis
Gaeumannomyces graminis ( Sacc . )
Arx & amp ; D.L .
Olivier – gatunek grzybów workowych z klasy `` Sordariomycetes '' .
Wywołuje zgorzel podstawy źdźbła .
Charakterystyka .
`` Gaeumannomyces graminis '' to homotaliczny grzyb mikroskopijny .
Zimuje w postaci grzybni w glebie na resztkach roślin .
Może tak przetrwać kilka lat aż do czasu ich mineralizacji .
Grzybnia ta jest głównym źródłem choroby .
Rozrastając się atakuje nowe rośliny przez włośniki i korzenie .
Po wrośnięciu do wnętrza korzenia grzybnia rozrasta się wewnątrz jego tkanek w górę pędu .
Poprzez wiązki przewodzące może dotrzeć aż do górnych międzywęźli .
Krótko przed zbiorami zbóż grzybnia wytwarza płciowe owocniki – perytecja .
Powstają w nich askospory nie odgrywające jednak większej roli w rozprzestrzenianiu się patogenu .
