Gagauzja
Gagauzja , Terytorium Autonomiczne Gagauzji ( mołd .
, , ) – obszar autonomiczny , położony w południowej części Mołdawii .
Gagauzja nie stanowi jednego spójnego obszaru , obejmuje teren pomiędzy miastami Komrat i Ceadîr-Lunga ( Çadır Lunga ) na północy oraz okolice miasta Vulcăneşti ( Valkaneş ) na południu , a także dwie enklawy o charakterze wiejskim ( Karbalia oraz Kıpçak ) .
Rdzenną ludność Gagauzji ( ponad 80 % ) stanowią Gagauzi – turkijskojęzyczni prawosławni chrześcijanie .
Geografia .
Gagauzja zajmuje terytorium 1832 km² , czyli nieco ponad 5 % całej powierzchni Republiki Mołdawii .
Położona jest w południowej części republiki .
Gagauzja po części sąsiaduje z Ukrainą .
Granica autonomii z Ukrainą stanowi 1/12 granicy Mołdawii z tym państwem .
Podzielona jest na 4 części , niepołączone ze sobą żadnym szlakiem .
Stolicą autonomii jest Komrat .
Obszar .
Obszar Autonomii Gagauskiej podlegał drobnym zmianom ponieważ wiele miejscowości , korzystając z możliwości wypowiedzenia się mieszkańców w drodze lokalnego referendum , przyłączało się do `` Gagauz Yeri '' bądź też z niej wychodziło .
Obligatoryjnie do Autonomii przypisane są tylko te punkty osadnicze , w których Gagauzi stanowią ponad 50 % mieszkańców .
Najnowsze dane dotyczące obszaru Gagauzji z końca roku 2006 to 1837,9 km² , czyli minimalnie więcej , niż podaje to większość źródeł .
Miejscowości .
Administracyjnie obszar Gagauzji dzieli się na : rejony ( `` dolay '' ) , miasta oraz wsie ( komuny ) .
Stolicą Gagauzji jest Komrat .
Liczba wsi ( komun ) przynależących do `` Gagauz Yeri '' wynosi 23 .
