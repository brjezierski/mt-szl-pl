Gajewniki-Kolonia
Gajewniki-Kolonia – wieś w środkowej Polsce położona w województwie łódzkim , w powiecie zduńskowolskim , w gminie Zduńska Wola .
Leży około 4 kilometry na wschód od Zduńskiej Woli oraz 38 kilometrów na południowy zachód od stolicy województwa – Łodzi & gt ; .
W bliskim sąsiedztwie znajduje się także wieś Gajewniki .
Populacja wsi wynosiła w 2009 roku 130 osób , w tym 60 kobiet i 70 mężczyzn .
W latach 1975–1998 miejscowość administracyjnie należała do województwa sieradzkiego .
