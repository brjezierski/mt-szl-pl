Gajlity
Gajlity ( niem .
`` Galitten '' ) – osada w Polsce położona w województwie warmińsko-mazurskim , w powiecie lidzbarskim , w gminie Lidzbark Warmiński .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
Osada znajduje się w historycznym regionie Warmia .
Wieś położona jest na zachodnim brzegu jeziora Blanki .
Historia .
Dawny majątek ziemski , w latach 1748–1780 był w posiadaniu rodu `` von Hatten '' , w pierwszej połowie XIX wieku w posiadaniu rodu `` von Knoblauch '' , natomiast pod koniec XIX w. – rodziny Langenstrassen .
Na początku XX w. majątek był w posiadaniu Richarda Brunka i obejmował blisko 300 ha ziemi uprawnej .
W skład dóbr wchodziła mleczarnia parowa ( prowadzono hodowlę bydła mlecznego rasy holenderskiej ) .
Do stycznia 1945 właścicielem majątku o powierzchni około 300 ha była rodzina `` Perk '' .
