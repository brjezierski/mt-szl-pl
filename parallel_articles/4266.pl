Gałacz
Gałacz ( rum .
`` Galați '' ) – jedno z największych miast we wschodniej Rumunii , położone na lewym brzegu Dunaju , pomiędzy ujściami Prutu i Seretu , stolica okręgu .
Historia .
Pierwsze wzmianki o Gałaczu pochodzą z XII wieku , miejscowość ta była znana w średniowieczu .
W XVIII wieku ważny port położony 150 km od ujścia Dunaju , mający przede wszystkim znaczenie w eksporcie drewna oraz zboża .
Gospodarka .
Największy rumuński port nad Dunajem , dostępny też dla statków morskich o mniejszej wyporności .
W mieście znajdują się największe w kraju : huta stali i stocznia .
Inne gałęzie przemysłu to maszynowy , metalowy , włókienniczy , spożywczy , drzewny , chemiczny , obuwniczy oraz materiałów budowlanych .
Inne informacje .
W Gałaczu znajduje się uczelnia `` Dunărea de Jos '' założona w 1948 roku , która w 1974 roku uzyskała status uniwersytecki .
Gałacz jest siedzibą Komisji Dunaju , organu regulującego żeglugę na rzece na podstawie Konwencji Belgradzkiej z 18 VIII 1948 roku .
Z Gałacza pochodzi piosenkarka popowa Giulia Anghelescu .
