Hełmówka woskowata
Hełmówka woskowata ( `` Galerina cerina '' A.H. Sm .
& amp ; Singer ) – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Galerina , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Wszystkie formy i odmiany to synonimy .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 0,5–1,5 cm , szeroko-stożkowaty do wypukłego .
Brzeg prążkowany od prześwitujących blaszek .
Powierzchnia naga , tylko na brzegu u młodych okazów nieco włókienkowata , w stanie wilgotnym ciemnopłowa , z jaśniejszym brzegiem , w stanie suchym wyblakła .
Przyrośnięte , początkowo bladobrązowe , potem płowe .
Wysokość 2–3 cm , grubość 2–3 mm , kruchy , cylindryczny , pogięty , w górnej części tej samej barwy co kapelusz i oprószony , w dolnej części płowy , z rozproszonymi białawymi fragmentami osłony , z wiekiem zanikającymi .
Bez wyraźnego zapachu i smaku .
Podstawki 27–35 x 8–10 μm , 4-sterygmowe .
Pileocystyd brak .
Cheilocystydy nabrzmiałe , główkowate 30–50 × 7 μm , z szyjką 12 × 3–5 μm , Sprzążki występują .
Zarodniki szeroko jajowate 8,7–12 × 5,5–7 μm , wrzecionowate lub wrzecionowato-główkowate , gładkie , bladobrązowe .
W wielu zarodnikach występują duże pęcherze .
Występowanie i siedlisko .
Hełmówka woskowata na półkuli północnej jest szeroko rozprzestrzeniona .
W piśmiennictwie naukowym na terenie Polski do 2003 r. podano tylko jedno stanowisko w Puszczy Niepołomickiej ( W. Wojewoda , 1999 ) .
Jej rozprzestrzenienie w Polsce i stopień zagrożenia nie są znane .
Występuje zazwyczaj w grupkach na torfowiskach wśród mchów , czasami także na próchnicy .
Owocniki pojawiają się wiosną i wczesnym latem .
