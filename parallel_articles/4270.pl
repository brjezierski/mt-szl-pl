Hełmówka piaskowa
Hełmówka piaskowa ( `` Galerina embolus '' ( Fr . )
P.D .
Orton ) – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Galerina , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1838 roku Elias Fries nadając mu nazwę `` Agaricus embolus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1960 roku Peter D. Orton .
