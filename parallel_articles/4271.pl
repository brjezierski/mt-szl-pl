Hełmówka drobna
Hełmówka drobna ( `` Galerina nana '' ( Petri ) Kühner ) – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Galerina , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1904 roku Lionell Petri nadając mu nazwę `` Naucoria nana '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1935 roku Robert Kühner .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Występowanie i siedlisko .
Występuje w niektórych krajach Europy , w Kanadzie ( Kolumbia Brytyjska ) i na Nowej Zelandii .
W piśmiennictwie naukowym na terenie Polski do 2003 r. podano jedno tylko stanowisko ( Góry Świętokrzyskie , 2002 ) .
Znajduje się na `` Czerwonej liście roślin i grzybów Polski '' .
Ma status E – gatunek wymierający , którego przeżycie jest mało prawdopodobne , jeśli nadal będą działać czynniki zagrożenia .
