Hełmówka prążkowana
Hełmówka prążkowana ( `` Galerina stylifera '' ( G.F .
Atk . )
A.H. Sm .
& amp ; Singer ) – gatunek grzybów należący do rodziny podziemniczkowatych ( `` Hymenogastraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Galerina , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1911 roku George Francis Atkinson nadając mu nazwę `` Galerula stylifera '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1958 roku Alexander Hanchett Smith i Rolf Singer .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 1,5–5 cm , u młodych owocników stożkowaty , potem wypukły z garbkiem .
Jest higrofaniczny .
W stanie wilgotnym powierzchnia pomarańczowo-brązowa lub cynamonowo-brązowa z silnie przeświecającymi blaszkami , w stanie suchym płowożółta do pomarańczowej , nieprzeźroczysta , błyszcząca , gładka , śliska .
