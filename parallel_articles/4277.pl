Gallatin ( rzeka )
Gallatin ( ang .
`` Gallatin River '' ) – rzeka przepływająca przez stany Wyoming i Montana w Stanach Zjednoczonych .
Długość około 193 km .
Źródło w Parku Yellowstone w północno-wschodnim Wyoming .
Łącząc się z rzekami Jefferson i Madison w Missouri Headwaters State Park w pobliżu miasta Three Forks w stanie Montana , tworzy oficjalny początek rzeki Missouri .
Nazwa rzeki pochodzi od nazwiska Alberta Gallatin , XIX .
wiecznego polityka .
Rzeka tworzy 110 km kanion pomiędzy pasmami górskimi Madison Range i Gallatin Range .
