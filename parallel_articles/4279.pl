Spodnie
Spodnie – wierzchnia część ubrania osłaniająca ciało od pasa w dół .
Noszone są zarówno jako element dwu- lub trzyczęściowego garnituru , jak i samodzielnie .
Dawniej były wyłącznie elementem ubrań męskich aż do lat 20 .
XX w. , kiedy emancypacja kobiet pociągnęła za sobą szereg przemian obyczajowych i rewolucję obyczajową .
Współcześnie są elementem ubrań zarówno kobiecych , jak i męskich .
Historia .
Ubranie podobne do spodni , bardziej przypominające rajstopy noszono już w pierwszych wiekach naszej ery , o czym świadczą znaleziska .
Stopy tych rajstopo-spodni podszyte były skórą tworząc podeszwy .
W drugiej połowie XII w. noszono nogawice , czyli dwie , niezszyte z sobą rury z materiału , najczęściej sukna , o dość zróżnicowanej długości przypinane do specjalnego paska zakładanego pod tunikę .
Pasek ten był męskim odpowiednikiem pasa do pończoch .
Nogawice na dole zawijane były onucami lub owijane krzyżującymi się na goleni taśmami albo wpuszczane w krótkie pończochy .
