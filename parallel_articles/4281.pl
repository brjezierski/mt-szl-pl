Gambia
Gambia ( pełna nazwa : Republika Gambii ; dawniej : Islamska Republika Gambii ) – państwo położone w zachodniej części kontynentu afrykańskiego .
Od północy , południa i wschodu graniczy z Senegalem .
Od zachodu posiada dostęp do Oceanu Atlantyckiego , przy ujściu rzeki Gambii .
Dawniej kolonia brytyjska , niepodległość uzyskała w 1965 r. Stolicą jest Bandżul ( ang .
`` Banjul '' ) .
Obecnie członek Unii Afrykańskiej i ECOWAS .
Geografia .
Gambia to najmniejsze pod względem powierzchni państwo kontynentalnej Afryki , zajmujące wąski , 50-kilometrowej szerokości skrawek lądu wzdłuż dolnego biegu rzeki Gambii .
Rzeka ta stanowi główny element krajobrazu Gambii .
Płynie w rozległej dolinie , którą otaczają niewysokie wzgórza i piaszczysty płaskowyż .
Gambia jest państwem o najniższej maksymalnej wysokości nad poziomem morza ( w zależności od źródeł 53 lub 64 m ) , leżącym w całości na stałym kontynencie .
Gambia położona jest w strefie klimatu zwrotnikowego z wysokimi opadami w porze deszczowej .
W dolinie rzecznej występują liczne tereny bagniste i namorzyny , a nad samym brzegiem w górnym biegu rzeki także lasy galeriowe .
Wyżej przeważa typowa dla Zachodniego Sudanu sawannowa roślinność trawiasta typu `` banto faros '' .
Historia .
Gambia stanowiła niegdyś część historycznych królestw Ghana i Songhaj .
Pierwsze wzmianki o tym obszarze pochodzą z IX i X w. od arabskich handlarzy , którzy przewozili przez Saharę niewolników , złoto i kość słoniową .
W XV w. pojawili się Portugalczycy , którzy przejęli kontrolę nad handlem , rozwijając morskie szlaki handlowe .
W tym czasie obszar dzisiejszej Gambii wchodził w skład państwa Mali .
O kontrolę nad ujściem rzeki Gambii współzawodniczyli Portugalczycy , Hiszpanie i Holendrzy , a później Anglicy i Francuzi .
Obszar ten był uznawany za idealne miejsce do penetracji wnętrza Afryki .
W latach 1651–1661 część terenów obecnej Gambii była kolonią Księstwa Kurlandii ( ówczesnego lenna I Rzeczypospolitej ) – ziemie u ujścia rzeki Gambii ( m.in .
