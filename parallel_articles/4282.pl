Game Freak
– japoński producent gier komputerowych współpracujący z firmą Nintendo .
Znany jest głównie z rozwijania serii konsolowych gier fabularnych `` Pokémon '' .
Firma została założona 26 kwietnia 1989 roku przez Satoshiego Tajiri .
Jednym z pierwszych produktów Game Freak była gra logiczna `` Quinty '' ( w Ameryce Północnej wydana pod nazwą `` Mendel Palace '' ) .
