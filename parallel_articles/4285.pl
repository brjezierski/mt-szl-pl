Gandia
Gandia ( ) – miasto i gmina liczące ok. 82 tysiące we Wspólnocie Autonomicznej Walencji , na wschodnim wybrzeżu Hiszpanii , nad Morzem Śródziemnym .
Znajduje się na odcinku wybrzeża zwanym Costa del Azahar , 65 km na południe od Walencji oraz 96 km na północ od Alicante .
Historia .
Gandia była istotnym centrum kulturalnym i handlowym w XV i XVI wieku , w XV wieku posiadała własny uniwersytet .
Gandia była miastem rodzinnym kilku znanych twórców , m.in .
Ausiàs March czy Joanot Martorell oraz katolickiego świętego Franciszka Borgiasza .
Współcześnie .
Obecnie Gandia jest jednym z największych miast wybrzeża w regionie , stanowi centrum handlu i turystyki .
Podzielona jest na dwie zasadnicze części - Gandia Miasto , w której znajdują się wszystkie historyczne zabytki , jak również centra handlowe , oraz Gandia Plaża , gdzie znajdują się apartamenty i letnie rezydencje , jak również większość barów i klubów nocnych .
Rejon plaży jest oddalony od centrum miasta o ok. 2 km .
Komunikacja .
W mieście znajduje się stacja kolejowa Gandia ( połączenia do Madrytu i Walencji ) .
Najbliższe lotnisko funkcjonuje w Walencji ( 75 km ) .
