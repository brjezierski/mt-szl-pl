Lakownica europejska
Lakownica europejska ( `` Ganoderma adspersum '' ( Schulzer ) Donk ) – gatunek grzybów należący do rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ganoderma , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1878 r. Schulzer nadając mu nazwę `` Polyporus adspersus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1969 r. Donk , przenosząc go do rodzaju `` Ganoderma '' .
Synonimy naukowe .
Nazwę polską podał Stanisław Domański w 1967 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako lakownica południowa .
Morfologia .
Wieloletni o kształcie kopytkowatym lub półokrągłym i rozmiarach 10–30 cm , grubości 4–10 cm .
Jest przyrośnięty bokiem .
Kapelusz na górnej powierzchni jest matowy , czerwonobrązowy , u starszych okazów brązowo-czarny .
