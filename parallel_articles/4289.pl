Lakownica brązowoczarna
Lakownica brązowoczarna ( `` Ganoderma carnosum '' Pat . )
– gatunek grzybów należący do rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ganoderma , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy :
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda w 1876 r. , Feliks Berdau w 1967 r. opisywał ten gatunek jako hubę lśniącą .
Morfologia .
Jednoroczny , typu huba , osiągający rozmiar 3–18 × 5–22 × 14 cm .
Składa się z trzonu i kapelusza .
Kapelusz bocznie osadzony , płaski , półokrągły lub nerkowaty .
Górna powierzchnia błyszcząca , jak gdyby lakierowana , u młodych owocników równa , u starszych pofałdowana , nierówna , guzkowata , o barwie od brązowej do czarnej .
Obrzeże , będące strefą aktywnego wzrostu ma początkowo barwę od białej do kremowej , u starszych okazów jest brązowe .
Trzon stożkowy , często spłaszczony , nieregularnie powyginany , o długości 1–10 cm i grubości 0,7–4 cm .
Jest przeważnie boczny , rzadko centralny , podobnie jak kapelusz błyszczący i ma barwę od brązowej do czarnej .
