Garapan
Garapan – census-designated place ( miejscowość spisowa ) w Marianach Północnych , na wyspie Saipan .
Według danych szacunkowych na rok 2009 liczyła 4414 mieszkańców .
Ośrodek turystyczny .
Piąta co do wielkości miejscowość kraju .
Siedziba oddziałów urzędów federalnych Stanów Zjednoczonych .
