Garbatka-Letnisko ( gromada )
Garbatka-Letnisko ( alt .
`` Garbatka Letnisko '' ) – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Garbatka-Letnisko siedzibą GRN w Garbatce-Letnisku utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie kozienickim w woj .
kieleckim , na mocy uchwały nr 13e/54 WRN w Kielcach z dnia 29 września 1954 .
W skład jednostki wszedł obszar dotychczasowej gromady Garbatka-Letnisko ze zniesionej gminy Policzna w tymże powiecie .
Dla gromady ustalono 27 członków gromadzkiej rady narodowej .
1 stycznia 1969 do gromady Garbatka-Letnisko przyłączono obszar zniesionej gromady Ponikwa .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 utworzono gminę Garbatka-Letnisko .
