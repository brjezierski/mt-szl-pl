Garcz ( przystanek kolejowy )
Garcz – nieczynny przystanek kolejowy w powiecie kartuskim między Garczem i Łapalicami .
Obiekt znajduje się na linii 229 , która łączy Pruszcz Gdański z Łebą .
Obok stacji znajduje się droga wojewódzka nr 211 oraz Jezioro Łapalickie .
Budynek dworcowy , zaadaptowany na mieszkania , posiada nietypową architekturę wzorowaną na dworkach szlacheckich .
Położenie .
Przystanek znajduje się w Garczu przy wyjeździe do Łapalic , pomiędzy Jeziorem Łapalickim a ulicą Kartuską , będącej częścią drogi wojewódzkiej nr 211 & lt ; ref name= '' ŚK1/2004 '' & gt ; & lt ; /ref & gt ; .
Historia .
Kolej dotarła do Garcza w 1905 , kiedy linię kolejową łączącą Pruszcz Gdański z Kartuzami przedłużono do Lęborka .
W Sieciowym Rozkładzie Jazdy Pociągów 96/97 przez Garcz przejeżdżało 6 par pociągów , ale już odcinek Pruszcz – Kartuzy był obsługiwany przez Komunikacje Autobusową .
Pod tabelą była wtedy zapisana uwaga `` Kursowanie pociągów i autobusów może być zawieszone po uprzednim ogłoszeniu '' .
Ruch został ostatecznie wstrzymany w czerwcu 2000 wraz z końcem obowiązywania rozkładu jazdy 1999/2000 , w którym przewidziane zostały tylko 2 pary .
