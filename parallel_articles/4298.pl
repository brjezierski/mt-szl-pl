Gareth Bale
Gareth Frank Bale ( ur .
16 lipca 1989 w Cardiff ) – walijski piłkarz , który występował na pozycji napastnika .
W latach 2006–2022 reprezentant Walii .
Kariera klubowa .
Southampton .
Gareth Bale swoją karierę piłkarską zaczynał w juniorskim zespole Southampton .
Do pierwszej drużyny został włączony w 2005 .
W seniorskim zespole zadebiutował 17 kwietnia następnego roku w ligowym spotkaniu z Millwall .
W trakcie pierwszego występu miał 16 lat i 275 dni , czym stał się drugim najmłodszym piłkarzem , który zadebiutował w pierwszej drużynie Southampton ( pierwszym był Theo Walcott ) .
Debiutancki sezon zakończył z dwoma ligowymi występami .
9 sierpnia w meczu z Derby County zdobył swojego pierwszego gola dla Southampton .
