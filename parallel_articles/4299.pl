Garmisch-Partenkirchen
Garmisch-Partenkirchen – powiatowa gmina targowa i uzdrowisko w południowych Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , siedziba powiatu Garmisch-Partenkirchen .
Miejscowość nie ma statusu miasta .
Leży w Alpach na trójstyku trzech pasm - Ammergauer Alpen , Bayerische Voralpen oraz Wettersteingebirge .
Jest znanym ośrodkiem narciarskim .
W miejscowości znajduje się stacja kolejowa Garmisch-Partenkirchen .
W Garmisch-Partenkirchen znajduje się początkowa stacja kolejki zębatej , którą można dostać się na najwyższy szczyt Niemiec Zugspitze 2962 m n.p.m .
( trasa częściowo wiedzie przez tunel wykuty w skale ) .
Historia .
Garmisch i Partenkirchen przez wieki były osobnymi miejscowościami rozdzielonymi rzeką Partnach oraz później stacją kolejową .
Połączono je dopiero w 1935 roku , na rozkaz Hitlera , przed zimowymi igrzyskami olimpijskimi które rozgrywano tu rok później .
Partenkirchen jest starsze – pierwsze zapisy na jego temat pochodzą z 15. roku naszej ery , kiedy było rzymskim miasteczkiem o nazwie `` Partanum '' .
Polityka .
Wójtem gminy jest Thomas Schmid z Christlich Soziales Bündnis - Bürger für Garmisch-Partenkirchen , rada gminy składa się z 30 osób .
Edukacja .
W GP mieści się Europejskie Centrum Studiów Bezpieczeństwa im .
George C. Marshalla ( George C. Marshall European Center for Security Studies ) .
Sport .
W mieście działa klub sportowy SC Riessersee .
W 1936 w Garmisch-Partenkirchen odbyły się IV Zimowe Igrzyska Olimpijskie .
Z polskich reprezentantów najwyższe 5. miejsce zajął w skokach narciarskich Stanisław Marusarz .
W 1978 i 2011 odbyły się Mistrzostwa Świata w Narciarstwie Alpejskim .
W miejscowości od 1953 co roku ( 1 stycznia ) odbywają się zawody skoków narciarskich w ramach Turnieju Czterech Skoczni na Große Olympiaschanze .
W 2008 roku skocznia została przebudowana .
