Garnat-sur-Engièvre
Garnat-sur-Engièvre – miejscowość i gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Allier .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 715 osób , a gęstość zaludnienia wynosiła 38 osób/km² .
W styczniu 2015 r. Garnat-sur-Engièvre zamieszkiwało 699 osób , przy gęstości zaludnienia wynoszącej 37,1 osób/km² .
