Garoowe
Garoowe ( arab .
`` Gharuwa '' ) – miasto w Somalii ; Stolica Puntlandu – autonomicznego regionu Somalii .
Czwarte co do wielkości miasto regionu ( po Boosaaso , Gaalkacyo i Laas Caanood ) .
33 395 mieszkańców ( 2005 ) .
Polska Akcja Humanitarna zdecydowała o utworzeniu stałej misji pomocowej , która ma w najbliższej przyszłości ułatwić dostęp mieszkańcom regionu do wody pitnej poprzez tworzenie nowych studni .
