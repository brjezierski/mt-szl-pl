Garwolin ( stacja kolejowa )
Garwolin – stacja kolejowa w Woli Rębkowskiej , około 5 km od centrum Garwolina , w województwie mazowieckim , w Polsce .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
Znajduje się tu 1 peron .
Linia kolejowa , przy której znajduje się stacja Garwolin została oddana do użytku w sierpniu 1877 i wtedy stacją obsługującą miasto Garwolin był przystanek kolejowy Wilga ( dzisiejsza Ruda Talubska ) .
Około 1910-1914 powstała stacja kolejowa Mirwan ( między Pilawą a stacją Wilga ) , a stacja Wilga została nazwana Garwolin .
