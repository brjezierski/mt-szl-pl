Gary ( Indiana )
Gary – miasto w Lake County , Indiana , Stany Zjednoczone , 25 mil ( 40 km ) od centrum Chicago , Illinois .
Historia .
Gary przylega do Parku Narodowego Indiana Dunes i graniczy z południowym jeziorem Michigan .
Gary został nazwany imieniem prawnika Elberta Henry'ego Gary'ego , który był prezesem założycielem United States Steel Corporation .
Miasto znane jest z dużych hut oraz jako miejsce narodzin zespołu muzycznego Jackson 5 .
Kiedyś drugie co do wielkości miasto w Indianie , w spisie powszechnym w 2010 roku miasto liczyło 80 294 mieszkańców , co czyniło go dziewiątym co do wielkości miastem w stanie Indiana .
Miasto od lat dwudziestych do połowy lat sześćdziesiątych XX wieku dobrze prosperowało ze względu na kwitnący przemysł stalowy , ale zagraniczna konkurencja i restrukturyzacja przemysłu stalowego spowodowały jego upadek i poważną utratę miejsc pracy .
Od końca lat 60 .
XX wieku Gary doświadczył drastycznego spadku liczby ludności .
Według raportu Business Insider z 2019 roku liczba ludności spadła do 75 000 z czego tylko połowa pracuje , a 36 % żyje w ubóstwie .
Miasto boryka się z trudnościami , w tym z bezrobociem , rozpadającą się infrastrukturą oraz niskim poziomem umiejętności czytania i pisania i wykształcenia .
Szacuje się , że prawie jedna trzecia wszystkich domów w mieście jest niezamieszkała lub opuszczona .
Gospodarka .
W mieście rozwinął się przemysł maszynowy , metalowy , chemiczny oraz hutniczy .
