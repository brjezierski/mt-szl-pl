Gateshead
Gateshead – miasto w północno-wschodniej części Anglii , w hrabstwie Tyne and Wear , w dystrykcie ( unitary authority ) Gateshead , położone na południowym brzegu rzeki Tyne , naprzeciw Newcastle upon Tyne ; stanowi część aglomeracji Tyneside .
W 2001 roku miasto liczyło 78 403 mieszkańców .
W mieście rozwinął się przemysł maszynowy , metalowy , spożywczy , lekki oraz hutniczy .
