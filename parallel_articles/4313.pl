Wnętrznica smardzowata
Wnętrznica smardzowata ( `` Gautieria morchelliformis '' Vittad . )
– gatunek grzybów z rodziny siatkoblaszkowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gautieria , Gomphaceae , Gomphales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1831 r. Carlo Vittadini .
Synonimy :
Polską nazwę zarekomendował Władysław Wojewoda w 2003 r .
Morfologia .
O średnicy 1–2,5 cm , początkowo kulisty do elipsoidalnego , potem nieregularnie klapowany , w stanie dojrzałym pozbawiony warstwy okołowierzchołkowej .
Perydium cienkie , białe , szybko zanikające .
Gleba miękka i gąbczasta , mocno pofałdowana , początkowo biała , potem przechodząca w ochrowobrązową , w końcu przebarwiająca się na ciemnobrązową .
