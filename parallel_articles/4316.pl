Gawrzyjałki
Gawrzyjałki ( dawniej `` Gawrzialken '' , od 1928 `` Wilhelmstahl '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie szczycieńskim , w gminie Szczytno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
Najważniejszy budynek to neogotycki kościół z 1908 r. W 1980 r. ewangelicka świątynia przejęta została przez społeczność katolicką , która napłynęła na Mazury po 1945 r. We wsi znajduje się również budynek przedwojennej szkoły o ciekawej bryle architektonicznej , w którym obecnie funkcjonuje niepubliczna szkoła podstawowa .
Gawrzyjałki są wsią o wyjątkowo dużej , jak na Mazury , liczbie krzyży i kapliczek .
Jest ich tam aż siedem .
Najważniejszą rolę poza kościołem we wsi odgrywa istniejąca od 1958 r. Ochotnicza Straż Pożarna .
Gawrzyjałki to wieś o bogatym życiu społecznym , skupionym głównie wokół kościoła i OSP .
Wieś założona na tak zwanych nowiznach lasów w nadleśnictwie Pupy w 1788 r. Ponad sto lat później , w 1895 r. , powstała w Gawrzyjałkach parafia .
Początkowo nabożeństwa odbywały się w szkole lub w pobliskim Białym Gruncie .
Kościół w Gawrzyjałkach wybudowano w 1907 roku , z okazji 200. rocznicy powstania państwa pruskiego .
W tamtym czasie wystrój kościoła był neogotycki .
W 1928 r. zmieniono urzędową nazwę wsi na `` Wilhemstahl '' .
W 1980 r. ewangelicką świątynię siłą przejęli katolicy .
