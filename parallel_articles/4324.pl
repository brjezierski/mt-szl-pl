Pruszcz Gdański ( stacja kolejowa )
Pruszcz Gdański – stacja kolejowa w Pruszczu Gdańskim , w województwie pomorskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca regionalnego .
Stacja została gruntownie zmodernizowana w l. 2012–2014 .
Modernizacja obejmowała przebudowę układu torowego , urządzeń srk oraz peronów , które zostały wydłużone .
Wykonawca został zobowiązany do pozostawienia oryginalnych wiat na peronach oraz zadaszeń zejść , nie zmieniła się także ich konstrukcja stalowa , położono natomiast nowe pokrycia oraz zadaszenie nad wejściem peronu 1 , jednak te elementy nie miały nawiązywać do istniejącej architektury .
Modernizacji poddano także przejście podziemne .
W ramach remontu tunelu zostały wybudowane cztery windy i pochylnia .
Przebudowa całej stacji kosztowała ponad 100 mln zł .
Z uwagi na bagnisty grunt w rejonie stacji , przy wytyczaniu nowej linii kolejowej do Kartuz rozpatrywano lokalizację stacji w innym miejscu .
W 1927 roku na stacji sprzedano 129 313 biletów .
Pociągi kursowały w kierunku Gdańska i z powrotem 18 razy na dobę .
W latach 30 .
XX wieku w budynku dworca mieściła się ochronka ( przedszkole ) dla polskich dzieci .
Od 11 grudnia 2016 pociągi trójmiejskiej SKM przestały kursować na odcinku Tczew – Gdańsk Główny .
29 maja 2017 podpisano umowę o dofinansowanie budowy na stacji w Pruszczu Gdańskim węzła integracyjnego .
