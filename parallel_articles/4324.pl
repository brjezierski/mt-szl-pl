W dniach 22-26 listopada 2021 PKP SA przeprowadziły rozbiórkę dotychczasowego dworca z 1994 w celu budowy w jego miejsce nowego .
W roku 2018 stacja obsługiwała 1500–2000 pasażerów na dobę .
