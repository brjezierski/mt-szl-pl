Gdynia
Gdynia ( , , ) – miasto na prawach powiatu w północnej Polsce , w województwie pomorskim , położone nad Morzem Bałtyckim , na Pobrzeżu Gdańskim i Pojezierzu Wschodniopomorskim .
Wchodzi w skład Trójmiasta ( wraz z Gdańskiem i Sopotem ) , jest zatem jednym z miast centralnych aglomeracji Trójmiasta .
Według stanu na koniec 2021 roku Gdynia liczyła ok. 244 000 mieszkańców ( dwunaste miejsce w Polsce ) i zajmowała powierzchnię ok. 135 km² .
Po nowym podziale działek terenu morskiego zwiększyła powierzchnię do 391 km² , z czego 66 proc .
