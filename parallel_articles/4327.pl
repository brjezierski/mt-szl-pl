Gwiazdosz baryłkowaty
Gwiazdosz baryłkowaty ( `` Geastrum lageniforme '' Vittad . )
– gatunek grzybów z rodziny gwiazdoszowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Geastrum , Geastraceae , Geastrales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten zdiagnozował w 1842 r. Carlo Vittadini .
Synonimy :
Komisja ds .
Polskiego Nazewnictwa Grzybów przy Polskim Towarzystwie Mykologicznym zarekomendowała polską nazwę gwiazdosz baryłkowaty .
Spotykana jest też nazwa gwiazdosz butelkowaty .
Morfologia .
Początkowo cebulowate z wyraźnie wydłużoną górną częścią .
Po osiągnięciu dojrzałości egzoperydium pęka 5–10 płatami o wydłużonych i ostro zakończonych szczytach .
Dolna ich część nie oddziela się od siebie , wskutek czego powstaje worek wewnątrz którego jest kulista główka endoperydium o średnicy do 2 cm .
Perystom na jej szczycie jest wyraźnie wyodrębniony , stożkowato wypukły i włókienkowaty .
Na zewnętrznej stronie rozpostartych ramion często widoczne są podłużne spękania .
Strzępki włośni o średnicy 5–13 µm , stopniowo zwężające się , z końcem ostrym lub zaokrąglonym , bladożółtobrązowe , miejscami i w kierunku końcówek czasem prawie szkliste , grubościenne , często stosunkowo szerokie , o średnicy 2–6 µm , powierzchni nieregularnie drobno inkrustowanej .
Zarodniki kuliste , żółtobrązowe do ciemnobrązowych , brodawkowate , o średnicy 3,4–4,5 µm , bez ornamentacji , brodawki przeważnie izolowane , nisko cylindryczne , proste lub lekko nieregularne , tępe , o wysokości 0,4–0,6 µm i szerokości 0,3–0,7 µm .
Występowanie i siedlisko .
Podano występowanie gwiazdosza baryłkowatego w Ameryce Północnej , Środkowej , Południowej , Europie , Korei i Japonii , Australii i na Nowej Zelandii .
W piśmiennictwie naukowym na terenie Polski do 2020 r. podano trzy stanowiska w 2006 , 2012 i 2020 r. W latach 1995–2004 objęty był ochroną częściową , a od roku 2004 – ochroną ścisłą bez możliwości zastosowania wyłączeń spod ochrony uzasadnionych względami gospodarki rolnej , leśnej lub rybackiej .
Grzyb saprotroficzny naziemny , czasami rozwijający się także na spróchniałym drewnie .
Występuje w lasach liściastych , głównie w grądach oraz pod topolami i robiniami .
Owocniki tworzy głównie od maja do sierpnia .
Są trwałe przez wiele tygodni .
