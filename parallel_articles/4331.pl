Pieniądz
Pieniądz – towar uznany w wyniku ogólnej zgody społecznej za środek wymiany gospodarczej , w którym są wyrażone ceny i wartości wszystkich innych towarów .
Jako waluta , krąży anonimowo od osoby do osoby i pomiędzy krajami , ułatwiając wymianę handlową .
Innymi słowy jest to materialny lub niematerialny środek , który można wymienić na towar lub usługę .
Prawnie określony środek płatniczy , który może wyrażać , przechowywać i przekazywać wartość ściśle związaną z realnym produktem społecznym .
Według współczesnej doktryny na pieniądz składają się trzy elementy :
Suma pieniężna jest wyrażana w jednostkach pieniężnych , zmaterializowanych w postaci znaków pieniężnych .
Ekonomiczne funkcje pieniądza .
Funkcja cyrkulacyjna ( transakcyjna ) .
Pieniądz jest powszechnym „ środkiem wymiany ” w transakcjach sprzedaży .
Każdy przyjmuje go za sprzedane towary i usługi , wiedząc o tym , że za pieniądze będzie mógł nabyć inne niezbędne mu dobra ( materialne lub usługi ) .
Dzięki pieniądzowi nastąpiło rozdzielenie w czasie transakcji kupna-sprzedaży na dwie odrębne czynności – transakcję kupna oraz transakcję sprzedaży – przez co przestała istnieć konieczność zachowania jedności czasu i miejsca .
Towar lub usługa mogą być sprzedane , a za otrzymane pieniądze można dokonać zakupu towaru lub usługi w innym miejscu oraz w innym terminie .
Funkcja obrachunkowa ( miernik wartości towarów ) .
Pieniądz jest „ miernikiem wartości ” .
Przy pomocy pieniądza możliwe jest wyrażenie wartości innych towarów ( w postaci cen towarów w jednostkach pieniężnych ) , ponieważ jest powszechnym ekwiwalentem .
Cena jest pieniężnym wyrazem wartości towarów i usług .
Wyrażenie wartości towarów i usług w pieniądzu wiąże się z siłą nabywczą pieniądza .
Aby określić cenę towaru lub usługi nie trzeba posiadać pieniądza , gdyż pełni on rolę miernika wartości również abstrakcyjnie .
Funkcja płatnicza .
Pieniądz jest „ środkiem płatniczym ” .
Pieniądz stał się środkiem płatniczym poprzez oddzielenie się ruchu towarów i świadczonych usług w czasie od ruchu pieniądza .
Dzieje się tak dlatego , że zapłata za towar lub usługę nie musi następować natychmiast po ich dostawie .
Zarówno sprzedający , jak i kupujący zazwyczaj umawiają się co do terminu zapłaty należności za dostarczone towary lub usługi .
Powstaje więc zobowiązanie odbiorcy wobec dostawcy , które wyrównane będzie w formie pieniężnej w terminie późniejszym .
Pieniądz spełnia funkcję środka płatniczego również przy pokrywaniu innych zobowiązań , jak np .
z tytułu podatków i opłat , wynagrodzeń pracowników , spłaty kredytów , darowizn itp .
