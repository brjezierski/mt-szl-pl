Giełgudyszki
Giełgudyszki ( lit .
`` Gelgaudiškis '' ) – miasto na Litwie , położone w okręgu mariampolskim , 15 km od Szaków .
Historia .
W XV wieku teren folwarku Giełgudyszki położonego na wysokim brzegu Niemna należał do królewskich dóbr skirstymońskich .
W 1504 r. król Aleksander Jagiellończyk darował je swojemu sekretarzowi Sapieżycowi protoplascie rodu Sapiehów .
Następnie dobra były własnością Massalskich , Dembińskich , Oziembłowskich , Giełgudów , Czartoryskich , baronów pruskich von Keudell , Komarów - którzy byli właścicielami do I wojny światowej .
W okresie międzywojennym majątek rozparcelowano , a w pałacu mieścił się dom dziecka , później szkoła .
Za Królestwa Polskiego istniała wiejska gmina Giełgudyszki .
Klasycystyczny pałac wybudowali w I poł .
XIX wieku von Keudellowie .
Po pożarze w 1979 r. pałac odbudowano , obecnie opuszczony .
Zachowało się kilka budynków dawnego majątku , które są otoczone ogrodem dworskim o pow .
24 ha i parkiem leśnym o łącznej powierzchni 112 ha porastającym zbocza doliny Niemna .
W Giełgudyszkach w 1812 roku urodził się Hieronim Kajsiewicz - polski kaznodzieja , współzałożyciel zakonu Zmartwychwstańców , pisarz religijny .
