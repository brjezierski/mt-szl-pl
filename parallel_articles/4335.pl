Gelnhausen
Gelnhausen – miasto powiatowe w Niemczech , w kraju związkowym Hesja , w rejencji Darmstadt , siedziba powiatu Main-Kinzig .
Historia .
Gelnhausen zostało założone przez Fryderyka I Barbarossę w roku 1170 , który zjednoczył trzy znajdujące się tu osady , otoczył je murem i nadał im status wolnego miasta .
Miejsce to zostało wybrane z powodu istniejącego tutaj skrzyżowania Via Regia , dróg królewskich pomiędzy Frankfurtem nad Menem a Lipskiem i kilku innych ważnych szlaków handlowych .
W tym samym czasie Gelnhausen uzyskało prawa miejskie , a zamek został wzniesiony na wyspie położonej na rzece Kinzig .
