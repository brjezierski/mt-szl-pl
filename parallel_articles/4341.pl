Gennevilliers
Gennevilliers – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Według danych na rok 2012 gminę zamieszkiwało 42 919 osób , a gęstość zaludnienia wynosiła 3 687 osób/km² .
Wśród 1287 gmin regionu Île-de-France Gennevilliers plasuje się na 295. miejscu pod względem powierzchni .
W mieście znajduje się stacja kolejowa Gare de Gennevilliers .
