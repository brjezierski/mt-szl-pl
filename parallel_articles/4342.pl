Genolier
Genolier – gmina w dystrykcie Nyon w kantonie Vaud w zachodniej Szwajcarii .
Historia .
Już w 1110 r. był wzmiankowany tutejszy kościół ( `` ecclesiam Genolliaci '' ) , ok. 1164 r. wspominany był `` Anselmus de Genollei '' , w 1184 r. `` Genoliacum '' , zaś w 1237 r. `` dominus Genulliaci '' .
Położenia .
Genolier leży u podnóży gór Jura , ok. 6 km .
na północ od siedziby dystryktu , Nyon , przy linii kolejowej Nyon-Saint-Cergue-Morez .
Klinika w Genolier .
Założona w 1972 roku `` Clinique de Genolier '' jest sztandarową placówką `` Swiss Medical Network '' .
Z prawie 120 łóżkami i 180 lekarzami jest to jedna z największych prywatnych klinik w Szwajcarii .
Oferuje wyspecjalizowaną i dostosowaną opiekę medyczną , najnowocześniejsze technologie , doświadczony zespół medyczny oraz najwyższej klasy usługi hotelowe .
`` Clinique de Genolier '' wyrobiła sobie solidną reputację w leczeniu raka dzięki swojemu ośrodkowi radioterapii , który jest jednym z najnowocześniejszych w Europie .
Jest również pierwszą placówką oferującą radioterapię śródoperacyjną ( IORT ) , innowacyjną metodę leczenia raka piersi .
Dzięki nowoczesnemu oddziałowi obrazowania medycznego klinika doskonale sprawdza się w chirurgii ogólnej i układu pokarmowego , ortopedii i chirurgii plastycznej .
Inne specjalności to ginekologia , neurologia i kardiologia .
