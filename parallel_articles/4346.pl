Gandawa
Gandawa ( niderl .
`` Gent '' , wym .
; fr .
`` Gand '' , wym . )
– miasto w północno-zachodniej części Belgii , w regionie Flandrii , stolica prowincji Flandria Wschodnia .
Leży przy ujściu rzeki Leie ( fr .
`` Lys '' ) do Skaldy , przez kanał Gandawa-Terneuzen ( niderl .
`` Kanaal Gent-Terneuzen '' ) ma dostęp do Morza Północnego .
Tworzy aglomerację , w skład której , prócz Gandawy , wchodzi 6 miast , największe z nich : Sint-Amandsberg , Gentbrugge .
W mieście działa polski konsulat honorowy .
Gminy tworzące Gandawę to : Gandawa-Centrum , Mariakerke , Drongen , Wondelgem , Sint-Amandsberg , Oostakker , Desteldonk , Mendonk , Sint-Kruis-Winkel , Ledeberg , Gentbrugge , Afsnee , Sint-Denijs-Westrem i Zwijnaarde .
