Genthin
Genthin − miasto w niemieckim kraju związkowym Saksonia-Anhalt , w powiecie Jerichower Land , nad kanałem Łaba-Hawela .
Miasto liczy 15 706 mieszkańców ( 2009 ) .
1 lipca 2009 przyłączono do miasta trzy gminy : Gladau , Paplitz i Tucheim .
Trzy lata później gminę Schopsdorf .
W mieście rozwinął się przemysł spożywczy oraz chemiczny .
