Ziemiozorek okazały
Ziemiozorek okazały ( `` Geoglossum cookeanum '' Nannf .
ex Minter & amp ; P.F .
Cannon ) – gatunek grzybów z monotypowej rodziny `` Geoglossaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Geoglossum , Geoglossaceae , Geoglossales , Incertae sedis , Geoglossomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Obowiązującą diagnozę taksonomiczną podali w 2015 r. John Axel Nannfeldt , David William Minter i Paul Francis , ale gatunek ten znany był już w 1879 r. Synonimy :
Polską nazwę zarekomendowała Komisja ds .
Polskiego Nazewnictwa Grzybów .
Morfologia .
Tworzy wysmukłe , czarne podkładki o wysokości 3–7 cm i bocznie spłaszczone lub z podłużnym rowkiem .
Składają się z części płodnej ( główki ) i cylindrycznego trzonu .
Maczugowata część płodna stanowi 50–70 % części podkładki i jest delikatnie szorstka .
Worki cylindryczne , 140–180 × 16–18 µm , dość grubościenne , 8-zarodnikowe .
Askospory wydłużone , cylindryczne do wrzecionowatych z ostrą podstawą i zaokrąglonym wierzchołkiem , dość grubościenne , gładkie , 55–90 × 5–7 µm z wieloma przegrodami ( bardzo rzadko 6-przegrodowe , ale zwykle 7-przegrodowe ) .
