Ziemiozorek czarniawy
Ziemiozorek czarniawy ( `` Geoglossum nigritum '' ( Pers . )
Cooke ) – gatunek grzybów z monotypowej rodziny `` Geoglossaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Geoglossum , Geoglossaceae , Geoglossales , Incertae sedis , Geoglossomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1797 r. Christiaan Hendrik Persoon , nadając mu nazwę `` Clavaria nigrita '' .
Obecną nazwę nadał mu Mordecai Cubitt Cooke w 1878 r .
Synonimy :
Nazwa rodzajowa `` Geoglossum '' pochodzi od słowa `` geo '' oznaczającego ziemię i `` glossum '' oznaczającego język .
Nazwa gatunkowa `` nigritum '' oznacza `` czarniawy '' .
Polską nazwę ziemiozorek czarniawy zarekomendowała Komisja ds .
Polskiego Nazewnictwa Grzybów .
