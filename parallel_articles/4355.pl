George Eliot
George Eliot , właśc .
Mary Ann Evans ( ur .
22 listopada 1819 w South Farm Arbury ( k. Nuneaton w Warwickshire , zm .
22 grudnia 1880 w Londynie ) – angielska pisarka należąca do czołowych twórców epoki wiktoriańskiej .
Autorka siedmiu powieści , cechujących się realizmem i przenikliwością psychologiczną ; ich akcja rozgrywa się najczęściej w prowincjonalnej Anglii .
Pisała także wiersze i inne utwory .
W słowniku języka angielskiego `` Oxford English Dictionary '' jest najczęściej cytowaną kobietą-pisarką .
Zaczerpnięto z jej utworów liczne przykłady ilustrujące zastosowanie angielskich słów .
W stworzonym w 2015 roku przez krytyków BBC rankingu 100 najlepszych brytyjskich powieści w historii , jej powieść `` Middlemarch '' zajęła pierwsze miejsce .
Życie i twórczość .
Wychowała się na prowincji w konwencjonalnej , konformistycznej i religijnej rodzinie w surowych zasadach protestantyzmu .
Dla Eliot krajobrazy Warwickshire stanowiły tło każdej powieści rozgrywającej się w prowincjonalnej Anglii .
Była piątym dzieckiem Roberta Evansa , zarządcy majątku Arbury Hall , budowniczego i geometry , znanego z prawego charakteru , pierwowzoru postaci Kaleba Gartha z `` Miasteczka Middlemarch '' .
Po śmierci matki przejęła jej obowiązki związane z prowadzeniem domu .
Najbardziej związała się z bratem Izaakiem przeniesionym na karty książki `` Młyn nad Flossą '' .
Uczęszczała do szkoły od piątego roku życia .
W wieku dziewięciu lat została przeniesiona najpierw na pensję panny Lewis , gorliwej ewangeliczki , a następnie do szkoły w Coventry , którą prowadziły siostry Franklin , kalwinki .
