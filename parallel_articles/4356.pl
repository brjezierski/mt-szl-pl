George Lucas
George Lucas , właściwie George Walton Lucas Junior ( ur .
14 maja 1944 w Modesto ) – amerykański reżyser , producent , scenarzysta , a także przedsiębiorca i filantrop oraz okazjonalnie aktor .
Odznaczony Narodowym Medalem Sztuki .
Jeden z najbardziej znanych twórców kasowych filmów w Ameryce .
Debiutem kinowym Lucasa był antytotalitarny `` THX 1138 '' , jednak popularność zyskał dzięki młodzieżowemu filmowi `` Amerykańskie graffiti '' .
Zasłynął stworzeniem popularnej serii `` Gwiezdne wojny '' , był także pomysłodawcą filmów i serialu o przygodach Indiany Jonesa .
Lucas założył firmy Lucasfilm ( produkcja filmów ) , Industrial Light and Magic ( efekty specjalne ) oraz LucasArts ( gry komputerowe ) .
Był czterokrotnie nominowany do Oscara .
W 1992 roku otrzymał nagrodę im .
Irvinga G. Thalberga .
George Lucas określa się w wywiadach jako metodysta-buddysta ( metodystą jest po rodzicach , a buddyzm to jego osobista fascynacja ) .
