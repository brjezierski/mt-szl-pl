George Michael , właśc .
Georgios Kyriacos Panayiotou ( ; ur .
25 czerwca 1963 w Londynie , zm .
25 grudnia 2016 w Goring-on-Thames ) – brytyjski piosenkarz , producent muzyczny i kompozytor muzyki pop i soul pochodzenia grecko-cypryjskiego i angielskiego .
Wczesne lata .
Urodził się w londyńskim East Finchley , w London Borough of Barnet , jako najmłodsze dziecko w rodzinie restauratora Jacka Panosa ( właśc .
Kyriacos Panajotu ) , greckiego imigranta urodzonego na Cyprze , oraz tancerki Lesley Angold ( z domu Harrison ; 1937–1997 ) będącą w połowie Angielką po ojcu ( George James Harrison ) i Żydówką ze strony matki ( Daisy Angold ) .
Pochodził z zamożnej rodziny .
Dorastał ze starszymi siostrami : Yodą ( ur .
1962 ) i Melanią ( 1960-2019 ) .
Matka została pochowana na prywatnej parceli w Highgate Cemetery .
Większość dzieciństwa spędził w Kingsbury w północno-zachodnim Londynie , gdzie uczęszczał do Kingsbury High School .
Gdy był nastolatkiem , jego rodzina przeniosła się do Radlett w Hertfordshire .
Tam uczęszczał do Bushey Meads School w sąsiednim mieście Watford , gdzie zaprzyjaźnił się z Andrew Ridgeleyem .
Obaj mieli podobne ambicje zrobienia kariery muzycznej .
Był zafascynowany twórczością Queen , Davida Bowiego , Eltona Johna , The Beatles i Led Zeppelin , poza tym regularnie nagrywał audycje `` Top 40 '' z listami muzycznych przebojów .
Z innym bliskim przyjacielem , gitarzystą Davidem Mortimerem , dorabiał , grając i śpiewając na stacji londyńskiego metra , a z Ridgeleyem zarabiali , roznosząc gazety i myjąc samochody .
Kariera muzyczna .
Działalność w zespole Wham !
W 1979 śpiewał w zespole The Executive , który założył z Andrew Ridgeleyem , przyjacielem ze szkoły `` Bushey Meads '' .
Dzięki występom z zespołem nabrał pewności siebie , zaczął także wierzyć w swój talent wokalny .
Po rozpadzie grupy imał się różnych zajęć , m.in .
sprzątał w restauracji ojca „ The Angus Pride ” , był magazynierem w „ British Home Stores ” , pracował na budowie oraz był bileterem w kinie „ Empire Cinema ” i didżejem w restauracji „ Bel Air ” .
W 1981 wraz z Ridgeleyem założył zespół Wham ! , w którym był głównym wokalistą , kompozytorem , a także okazjonalnie instrumentalistą i producentem .
W 1982 nawiązali współpracę z niezależną wytwórnią Innervision Records i wydali debiutancki singel „ Wham Rap !
( Enjoy What You Do ) ” , po którego premierze Michael zaczął posługiwać się swoim anglojęzycznym pseudonimem artystycznym .
W tym samym roku Wham !
wystąpili w programie telewizyjnym `` Top Of The Pops '' , podczas którego wykonali piosenkę „ Young Guns ( Go For It ! ) ” .
Występ ten uważany jest do dziś za jeden z najlepszych w historii zespołu , jak również całego programu .
