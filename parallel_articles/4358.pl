George Orwell
George Orwell , właściwie Eric Arthur Blair ( ur .
25 czerwca 1903 , zm .
21 stycznia 1950 ) – brytyjski pisarz i publicysta , uczestnik hiszpańskiej wojny domowej .
Urodzony w Motihari w Bengalu , do Anglii przyjechał w 1907 .
Jego dzieła odzwierciedlają inteligencję , dowcip i wrażliwość na nierówności społeczne autora .
Zagorzały krytyk systemów totalitarnych , zwolennik socjalizmu demokratycznego .
Często uznawany za najlepszego kronikarza angielskiej kultury XX wieku , Orwell pisał powieści i felietony ; zajmował się także krytyką literacką .
Za jego najbardziej znane dzieła uznawane są : futurystyczna , antyutopijna powieść `` Rok 1984 '' i satyryczna opowieść będąca alegorią systemu totalitarnego `` Folwark zwierzęcy '' .
Obie te książki sprawiły , że Orwell należy do grona najbardziej popularnych pisarzy XX wieku .
Wywarł istotny wpływ na współczesną kulturę .
Przymiotnik „ orwellowski ” określa sytuacje , w których system zagraża wolności jednostki bądź całych społeczeństw , szczególnie w zakresie prywatności .
Zwroty takie jak „ Wielki Brat patrzy ” , czy „ Wszystkie zwierzęta są równe , ale niektóre są równiejsze ” oraz takie pojęcia , jak „ nowomowa ” ( „ new-speak ” ) , „ policja myśli ” ( „ thought police ” ) , „ dwójmyślenie ” ( „ doublethink ” ) i „ myślozbrodnia ” ( „ thoughtcrime ” ) są używane w potocznym języku .
Powieść `` Rok 1984 '' była także inspiracją dla stworzenia popularnego reality-show Big Brother , emitowanego w kilkunastu krajach .
Życiorys .
Jego rodzina pochodziła z klasy średniej .
Był synem Richarda Walmesley Blaira , urzędnika administracji brytyjskiej i Idy Mabel Blair ( z d. Limouzin ) .
Miał dwie siostry .
Od 1911 uczęszczał do St. Cyprian ’ s , prywatnej szkoły z internatem w Eastbourne .
Stypendium ( przyznane w 1917 ) zapewniło mu miejsce w ekskluzywnej Eton College ( którą opuścił w 1921 ) .
W listopadzie 1922 wstąpił do Królewskiej Policji Imperialnej w Birmie .
1 stycznia 1928 porzucił służbę i nie powrócił z urlopu w Anglii .
W latach 1928–1929 przebywał w Paryżu , gdzie opublikował swoje pierwsze artykuły .
W styczniu 1933 opublikował pierwszą książkę pod pseudonimem literackim `` George Orwell '' , była to powieść '' Na dnie w Paryżu i w Londynie '' .
Przybrał pseudonim , ponieważ chciał oszczędzić rodzinie problemów z powodu ujawnienia w książce prywatnych szczegółów ze swojego życia .
Powodem takiej decyzji był także fakt , że uważał swoją książkę za niewiele wartą .
Poza tym nie lubił swojego prawdziwego nazwiska .
