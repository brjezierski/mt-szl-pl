George Vancouver
George Vancouver ( ur .
22 czerwca 1757 w King ’ s Lynn , Norfolk , zm .
10 maja 1798 w Petersham koło Richmond upon Thames , Surrey ) − brytyjski oficer Royal Navy , podróżnik i odkrywca .
Z pochodzenia Holender ( Van Coeverden ) .
Uczestniczył w drugiej wyprawie Cooka na statku „ Resolution ” ( trafił tam przez znajomości rodzinne ) , podczas której badano obszary podbiegunowe ( Antarktyda ) .
Następnie brał udział w trzeciej wyprawie Cooka ( 1780 ) , podczas której miano sprawdzić , czy jest możliwe opłynięcie Ameryki od północy .
Cook zginął w 1779 roku na Hawajach , a próbę odkrycia Przejścia Północno-Zachodniego podjął Vancouver .
Badał pacyficzne wybrzeża Ameryki Północnej wzdłuż Oregonu , Waszyngtonu i Kolumbii Brytyjskiej w latach 1791-1795 .
Jego nazwiskiem nazwano wyspę Vancouver u brzegów Kanady i miasto Vancouver .
Upamiętnia go również nazwa naukowa rodzaju roślin z rodziny berberysowatych – `` Vancouveria '' .
