George W. Bush
George Walker Bush ( wym .
; ur .
6 lipca 1946 w New Haven ) – amerykański polityk , 43. prezydent Stanów Zjednoczonych , 46. gubernator Teksasu .
Jest synem 41. prezydenta , George ’ a H.W .
Busha .
Po ukończeniu studiów na Uniwersytecie Yale w 1968 oraz w Harvard Business School w 1975 pracował na stanowiskach kierowniczych w przedsiębiorstwach naftowych .
Był współwłaścicielem klubu Texas Rangers , a następnie sprawował urząd gubernatora stanu Teksas ( 1995–2000 ) .
W 2000 roku uzyskał nominację Partii Republikańskiej i wygrał w kontrowersyjnych wyborach prezydenckich .
Objął urząd prezydenta Stanów Zjednoczonych 20 stycznia 2001 , kilka miesięcy przed zamachami z 11 września .
W ich konsekwencji George W. Bush ogłosił strategię walki w ramach wojny z terroryzmem , zakładającej operacje wojskowe w Afganistanie ( od 2001 ) oraz inwazję na Irak ( 2003 ) .
W 2004 został wybrany na drugą kadencję , która upłynęła w 2009 .
Życiorys .
Młodość i wykształcenie .
Wychowywał się w Midland i Houston w Teksasie , z czwórką rodzeństwa : Jeb , Neil , Marvin i Dorothy .
