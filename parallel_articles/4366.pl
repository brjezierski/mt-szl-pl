Gerard Badía
Gerard Badía Cortés ( ur .
18 października 1989 w Horta de Sant Joan ) – hiszpański piłkarz grający na pozycji pomocnika , zawodnik hiszpańskiego klubu .
Przebieg kariery .
Badía grę w piłkę zaczynał w CD Tortosa , skąd latem 2008 przeniósł się do CF Gavà .
Rok później został zawodnikiem rezerw Realu Murcia .
Po kolejnym roku zdecydował się podpisać kontrakt z CD Guadalajara , w której występował przez trzy lata .
Latem 2013 związał się z SD Noja .
W lutym 2014 został na pół roku wypożyczony do Piasta Gliwice , a latem tego samego roku trafił do klubu na stałe .
Łącznie przed siedem i pół sezonu rozegrał w barwach gliwickiego klubu 214 spotkań , w których strzelił 31 goli oraz zanotował 40 asyst .
Razem z Piastem zdobył złoty , srebrny i brązowy medal mistrzostw Polski , a od 2017 był kapitanem zespołu .
Przez ten czas bardzo związał się z klubem i miastem , został też wybrany przez kibiców i ekspertów do Galerii Legend Ekstraklasy .
14 lipca 2021 roku podpisał kontrakt z hiszpańskim .
