Gerard Cieślik
Gerard Józef Cieślik ( ur .
29 kwietnia 1927 w Hajdukach Wielkich , zm .
3 listopada 2013 w Chorzowie ) – polski piłkarz , występujący na pozycji napastnika , reprezentant Polski w latach 1947–1958 , olimpijczyk , trener piłkarski .
W czasie II wojny światowej był zawodnikiem reaktywowanego w 1939 roku Bismarckhütter SV 99 .
Po powrocie z frontu podpisał akces do gry w Ruchu Chorzów , dla którego występował przez blisko piętnaście lat .
W barwach Ruchu trzykrotnie zdobył mistrzostwo Polski ( 1951 , 1952 , 1953 ) i jeden raz Puchar Polski ( 1950/51 ) .
Dwukrotny król strzelców rozgrywek I ligi ( 1952 , 1953 ) .
Z reprezentacją Polski uczestnik Igrzysk Olimpijskich 1952 w Helsinkach .
Członek Klubu Wybitnego Reprezentanta oraz z dorobkiem 168 goli Klubu 100 .
Będąc czynnym piłkarzem pełnił funkcje szkoleniowe w Grunwaldzie Ruda Śląska , Prośnie Wieruszów i Concordii Knurów .
Po zakończeniu kariery zawodniczej trener Ruchu Chorzów , MKS–u Lędziny , Unii Racibórz , AKS–u Chorzów oraz Uranii Ruda Śląska .
Kariera piłkarska .
Młodość i początki kariery ( 1927–1945 ) .
Gerard Cieślik urodził się 29 kwietnia 1927 roku w Hajdukach Wielkich w rodzinie Marty z domu Dyl i Antoniego .
Ojciec był pracownikiem huty .
Wraz z innymi pracownikami został przed rozpoczęciem II wojny światowej ewakuowany na wschód kraju , gdzie miał być przeniesiony polski przemysł ciężki .
Zginął w drodze do Sandomierza podczas bombardowania pod Olkuszem .
Matka była działaczką Towarzystwa Gimnastycznego `` Sokół '' w Hajdukach Wielkich , a po śmierci męża pracowała w hucie .
Gerard był ich drugim dzieckiem .
