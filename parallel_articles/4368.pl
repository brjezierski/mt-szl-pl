Gerard Damiano
Gerardo Rocco „ Gerard ” Damiano ( ur .
4 sierpnia 1928 w Nowym Jorku , zm .
25 października 2008 w Fort Myers ) – amerykański reżyser , producent filmowy i scenarzysta , znany przede wszystkim z filmu pornograficznego `` Głębokie gardło '' ( 1972 ) z Lindą Lovelace i Harry Reemsem .
Film ten był skandalem obyczajowym , a jednocześnie jednym z najbardziej dochodowych filmów w historii kina ( koszt 25 tysięcy dolarów , przychód 600 milionów dolarów ) .
Wyreżyserował także przebój `` Diabeł w pannie Jones '' ( `` The Devil in Miss Jones '' , 1973 ) , który zajął 7 miejsce na liście najbardziej dochodowych zdjęć magazynu „ Variety ” z 1973 roku ( Głębokie gardło zajęło 11 miejsce w tym roku , drugi rok z rzędu na liście ) .
Damiano jest jednym z przełomowych reżyserów tak zwanego `` Złotego Wieku Porno '' ( 1969–1984 ) .
Życiorys .
Wczesne lata .
Urodził się w nowojorskim Bronx w rodzinie katolickiej włoskich imigrantów .
Gdy miał sześć lat , zmarł jego ojciec .
W młodości pracował na Times Square jako chłopiec do czyszczenia obuwia , a także na Manhattan Automat jako boy hotelowy .
W wieku 17 lat wstąpił do United States Navy SEALs , służąc przez cztery lata .
Po opuszczeniu marynarki , Damiano pracował 12 lat jako technik RTG w szpitalu Jamaica Hospital w Queens .
Następnie otworzył salon fryzjerski z przyjacielem i przez wiele lat pracował jako fryzjer w małym zakładzie fryzjerskim na Manhattanie .
Jako zapalony fotograf amator , obsługiwał wesela i wykonywał zdjęcia dzieciom .
Kariera .
W latach 60. na sugestię swojego księgowego Damiano rozpoczął karierę filmową .
Początkowo jako asystent .
W wieku czterdziestu lat debiutował dramatem `` We All Go Down '' ( 1969 ) .
Był reżyserem takich filmów 16 mm i Super 16 mm pod pseudonimem Jerry Gerard .
Były to m.in .
: `` Diabeł w pannie Jones '' ( `` The Devil in Miss Jones '' , 1973 ) czy `` Memories Within Miss Aggie '' ( 1974 ) .
Inne jego realizacje to : `` Teenie Tulip '' ( 1970 ) , `` The Story of Joanna '' ( 1975 ) , `` For Richer , For Poorer '' ( 1979 ) , `` Consenting Adults '' ( 1982 ) , `` Future Sodom '' ( 1987 ) , `` Young Girls in Tight Jeans '' ( 1989 ) , `` Dirty Movies '' ( 1989 ) i `` Naked Goddess 2 '' ( 1994 ) .
Był podziwiany przez innych profesjonalistów filmowców , takich jak Sergio Leone czy John Cassavetes .
W styczniu 2009 Jaime Noguera , dyrektor Międzynarodowego Festiwalu Filmów Krótkometrażowych i Alternatywnych w Benalmádenie ( Festival Internacional de Cortos y Cine Alternativo de Benalmádena FICCAB ) , zlecił napisanie książki Paco Gisbertowi `` Gerard Damiano : niezależny pornograf '' ( `` Gerard Damiano : el pornógrafo indie '' ) , która ukazała się 1 listopada 2009 .
W dramacie biograficznym `` Królowa XXX '' ( `` Lovelace '' , 2013 ) w reżyserii Jeffreya Friedmana i Roba Epsteina jego rolę jako reżysera filmu `` Głębokie gardło '' zagrał Hank Azaria .
Życie prywatne .
