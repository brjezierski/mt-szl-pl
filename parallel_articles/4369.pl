Gierałtowice ( gromada w powiecie rybnickim )
Gierałtowice – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Gierałtowice z siedzibą GRN w Gierałtowicach utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie rybnickim w woj .
