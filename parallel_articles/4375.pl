Girona
Girona ( katalońska wymowa : [ ] ; hiszp .
`` Gerona '' , wym .
[ ] ) – miasto w północno-wschodniej Hiszpanii , w Katalonii ; stolica prowincji o tej samej nazwie i comarki Gironès .
Ma jedną z najlepiej zachowanych średniowiecznych starówek w Hiszpanii , z jedną z największych historycznych dzielnic żydowskich w Europie .
Historia .
Miasto zostało założone przez rzymskiego wodza Pompejusza w czasie wojny Rzymu z Sertoriuszem około roku 79 p.n.e .
Osada wytyczona została na planie trójkąta , którego jeden bok opierał się na rzece Onyar , natomiast przeciwległy wierzchołek stanowiła wieża `` Torre Gironella '' .
Była ona otoczona murami miejskimi i liczyła około trzech tysięcy mieszkańców .
