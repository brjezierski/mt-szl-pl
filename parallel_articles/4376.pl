Fermentacja
Fermentacja – enzymatyczny proces przemian związków organicznych w warunkach beztlenowych , prowadzony przez mikroorganizmy , np .
bakterie lub drożdże , w efekcie którego następuje dysproporcjonowanie substratu , tj .
jego równoczesne utlenienie i redukcja , np .
:
Procesowi temu towarzyszy wydzielenie energii , która mazgazynowana jest zazwyczaj pod postacią ATP , powstającego w wyniku fosforylacji substratowej , lub , wyjątkowo , w wyniku działania pomp transbłonowych : wodorowej lub sodowej .
Fermentacja umożliwia uzyskanie energii użytecznej metabolicznie organizmom stale lub okresowo żyjącym w warunkach beztlenowych .
Wydajność energetyczna fermentacji jest znacznie niższa niż procesów tlenowych .
Z jednej cząsteczki glukozy uzyskiwane są 2 ( czasem 1–2,5 ) cząsteczki ATP , podczas gdy w cyklu Krebsa z jednej cząsteczki glukozy uzyskiwane są 32 ( lub 38 ) cząsteczki ATP .
Innym rodzajem beztlenowego uzyskiwania energii jest oddychanie beztlenowe , które różni się od fermentacji tym , że zachodzi z udziałem łańcucha transportu elektronów , który nie jest wykorzystywany w procesach fermentacyjnych .
