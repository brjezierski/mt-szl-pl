Ghana
Ghana ( Republika Ghany , ang .
`` Republic of Ghana '' ) – państwo w zachodniej Afryce nad Oceanem Atlantyckim .
Graniczy z Togo , Burkina Faso i Wybrzeżem Kości Słoniowej .
Nazwa kraju w języku Asante oznacza „ Król – Wojownik ” .
Pochodzi od przedkolonialnego , średniowiecznego Imperium Ghany , które rozciągało się na terenach dzisiejszych Mali i Mauretanii .
Handel z Europejczykami został po raz pierwszy nawiązany w XV w. , natomiast przedkolonialne królestwo Aszanti zachowało niepodległość do roku 1870 , w którym to zostało skolonizowane przez Wielką Brytanię .
Niepodległość odzyskała w roku 1957 .
Obecnie członek Unii Afrykańskiej i ECOWAS .
Kraj szczyci się jedną z najbardziej stabilnych i najlepiej prosperujących gospodarek na kontynencie i jest drugim co do wielkości producentem kakao na świecie .
Jezioro Wolta zbudowane w 1965 roku , jest największym sztucznym zbiornikiem wodnym na świecie .
Ustrój polityczny .
Ghana jest republiką należącą do Wspólnoty Narodów ( od 1957 r. ) .
Na czele państwa stoi prezydent ( od 7 stycznia 2017 Nana Akufo-Addo ) , który sprawuje władzę wykonawczą .
Parlament Ghany jest jednoizbowy i zdominowany przez 2 partie – Nową Partię Patriotyczną oraz Narodowy Kongres Demokratyczny .
Kofi Annan , były sekretarz generalny ONZ pochodzi z Ghany .
Siły zbrojne .
Obecnie ghańskie siły zbrojne są jednymi z najlepiej zorganizowanych , wyposażonych i wyszkolonych w tym regionie Afryki .
Daje to Ghanie tym samym status regionalnego mocarstwa .
Duży wpływ na taką sytuację ma bliska współpraca z siłami zbrojnymi Chińskiej Republiki Ludowej .
Ghana dysponuje trzema rodzajami sił zbrojnych : wojskami lądowymi , marynarką wojenną oraz siłami powietrznymi .
Uzbrojenie sił lądowych Ghany składało się w 2014 roku m.in .
z 125 opancerzonych pojazdów bojowych .
Marynarka wojenna Ghany dysponowała w 2014 roku 20 okrętami obrony przybrzeża .
Ghańskie siły powietrzne z kolei posiadały w 2014 roku uzbrojenie w postaci m.in .
17 samolotów transportowych , pięć samolotów szkolno-bojowych oraz 14 śmigłowców .
Wojska ghańskie w 2014 roku liczyły 13,5 tys .
