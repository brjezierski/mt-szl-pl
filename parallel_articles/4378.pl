Ghazni ( prowincja )
Ghazni ( perski : غزنى ) – jedna z 34 prowincji afgańskich .
Stolica w Ghazni leżącym na trakcie handlowym Kabul-Kandahar .
W 2021 roku zamieszkiwało ją prawie 1,4 mln osób .
Warunki naturalne .
Prowincja o dużym zróżnicowaniu powierzchni , a co za tym idzie sposobów użytkowania ziemi .
Ze względu na sąsiedztwo zbierających wilgoć wysokich gór jest tam stosunkowo jak na ten kraj dobrze rozwinięta sieć rzeczna .
Góry zbierające wilgoć i rzeki są jednak przyczyną częstych powodzi .
Położona w newralgicznym miejscu Afganistanu była częstym obiektem najazdów .
