Ghilarza
Ghilarza – miejscowość i gmina we Włoszech , w regionie Sardynia , w prowincji Oristano .
Graniczy z Abbasanta , Aidomaggiore , Ardauli , Bidonì , Boroneddu , Busachi , Fordongianus , Norbello , Paulilatino , Sedilo , Soddì , Sorradile , Tadasuni i Ula Tirso .
Według danych na rok 2004 gminę zamieszkiwało 4379 osób , 82,6 os./km² .
