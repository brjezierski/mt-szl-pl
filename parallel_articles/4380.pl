Fusarium fujikuroi
Fusarium fujikuroi Nirenberg – gatunek workowców należący do rodziny gruzełkowatych ( `` Nectriaceae '' ) .
Gatunek szeroko rozprzestrzeniony na całym świecie .
Występuje w glebie oraz na porażonych roślinach i ich nasionach .
Wywołuje choroby zwane fuzariozami :
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Fusarium , Nectriaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimów ma ponad 30 .
Niektóre z nich :
Morfologia .
Perytecja tworzy zwykle tylko na martwym podłożu organicznym .
Powstają na powierzchni , mają grubą ścianę zewnętrzną , barwę ciemnoniebieską , szarą lub stalową , wysokość 0,25-0,3 mm i szerokość 0,22-0,3 mm .
Worki elipsoidalne z 4-8 ukośnie ułożonymi , hialinowymi , eliptycznymi askosporami o rozmiarach 14–18 × 4,5–6 μm .
Zazwyczaj mają 1 przegrodę , ale czasami 3 .
Kolonia ma barwę ciemnofioletową , liliową lub winną .
