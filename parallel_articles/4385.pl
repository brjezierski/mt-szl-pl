Giełczyce
Giełczyce ( niem .
`` Geltendorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Skoroszyce .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Gelczicz '' .
