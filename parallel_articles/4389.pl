Gilles Marguet
Gilles Marguet ( ur .
3 grudnia 1967 w Pontarlier ) – francuski biathlonista , brązowy medalista olimpijski i dwukrotny medalista mistrzostw świata .
Kariera .
W Pucharze Świata zadebiutował 19 stycznia 1990 roku w Anterselvie , zajmując 15. miejsce w sprincie .
Tym samym już w swoim debiucie zdobył pierwsze punkty .
Na podium zawodów tego cyklu jedyny raz stanął 16 stycznia 1993 roku w Val Ridanna , kończąc rywalizację w sprincie na trzeciej pozycji .
Wyprzedzili go tam jedynie Włoch Johann Passler oraz Ulf Johansson ze Szwecji .
Najlepsze wyniki osiągnął w sezonie 1989/1990 , kiedy zajął 21. miejsce w klasyfikacji generalnej .
Na mistrzostwach świata w Borowcu w 1993 roku wspólnie z Thierrym Dusserre'em , Xavierem Blondem i Lionelem Laurentem zdobył brązowy medal w biegu drużynowym .
Podczas mistrzostw świata w Pokljuce w 2001 roku reprezentacja Francji w składzie : Gilles Marguet , Vincent Defrasne , Julien Robert i Raphaël Poirée zwyciężyła w sztafecie .
Był to pierwszy w historii złoty medal dla Francji w tej konkurencji .
Brał też udział w igrzyskach olimpijskich w Salt Lake City , gdzie razem z Defrasne'em , Robertem i Poirée zdobył brązowy medal w biegu sztafetowym .
Wziął również udział w sprincie , kończąc rywalizację na 66. miejscu .
Były to jego jedyne starty olimpijskie .
W 1995 roku wywalczył srebrny medal w sprincie podczas mistrzostw Europy w Grand-Bornand , gdzie lepszy był tylko Poirée .
