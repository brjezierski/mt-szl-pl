Nazwa .
W Domesday Book z roku 1086 miejscowość występuje pod nazwą `` Gelingham '' .
W historii pisownia nazwy miasta zmieniała się jeszcze co najmniej pięciokrotnie i pochodzi od osoby lub rodziny zwanej Gylla .
