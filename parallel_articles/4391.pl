W skład jednostki wszedł obszar dotychczasowej gromady Gilowice ze zniesionej gminy Gilowice w tymże powiecie .
Dla gromady ustalono 27 członków gromadzkiej rady narodowej .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 reaktywowano gminę Gilowice .
