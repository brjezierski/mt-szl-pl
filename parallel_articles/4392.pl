Nabijarka gilz
Nabijarka gilz , gilzownica – przyrząd lub urządzenie do napełniania gilz tytoniem i wytwarzania w ten sposób papierosów .
Istnieją nabijarki ręczne oraz elektryczne .
Gilzy papierosowe są pustymi bibułkowymi tubkami z przyklejonym do nich filtrem ( dawniej bez filtra , ewentualnie tylko z ustnikiem ) , które można gotowe kupić w sklepach .
Osobny zakup gilz i tytoniu i wykonanie z nich w warunkach domowych papierosów obniża koszt produktu końcowego .
