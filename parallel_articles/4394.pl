Gimpo ( 1992 )
Gimpo ( 김포 ) – południowokoreański niszczyciel min z lat 90 .
XX wieku , jedna z sześciu zbudowanych jednostek typu `` Ganggyeong '' .
Okręt został zwodowany 29 września 1992 roku w stoczni Kangnam Corporation w Pusan , a do służby w Marynarce Wojennej Republiki Korei wszedł 30 kwietnia 1993 roku .
Jednostka , oznaczona numerem taktycznym 565 , nadal znajduje się w składzie floty i ma status operacyjny ( stan na 2019 rok ) .
Projekt i budowa .
Niszczyciele min typu `` Ganggyeong '' zostały zaprojektowane w krajowej stoczni Kangnam , jednak wzorowane były na włoskich jednostkach typu `` Lerici '' .
Pierwsza jednostka – „ Ganggyeong ” – przeznaczona została do testów , dwie następne zamówiono w 1988 roku , a trzy kolejne w roku 1990 .
Plany zbudowania łącznie 16 okrętów tego typu zostały zarzucone .
