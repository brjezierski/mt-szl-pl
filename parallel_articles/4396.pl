16 : `` Giornale del Provveditore '' , c. 87 r. na Campo de ’ Fiori , gdzie później postawiono mu pomnik ; .
Jego zuchwałe i pogardliwe drwiny z dogmatów dały mu niezrównany rekord ekskomunik : oprócz katolickiej także kalwińską i luterańską .
