Bruno jest znany ze swoich spekulacji dotyczących astronomii – w tym kosmologii – które opierały się na heliocentrycznej teorii Kopernika i na zasadzie kopernikańskiej .
Podejrzewał istnienie egzoplanet – gwiazdy miałyby być ciałami niebieskimi takimi jak Słońce , otoczonymi przez swoje własne planety .
Twierdził również , że Wszechświat jest nieskończony .
Jego hipotezy wyprzedzały ówczesne czasy i zapowiadały nowożytną naukę .
Jego poglądy religijne były bliskie panteizmowi .
