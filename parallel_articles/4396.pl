Philippo Bruno urodził się w rodzinie katolickiej .
Był synem żołnierza Giovanniego Bruna i Fraulisy Savolino .
Philippo w wieku 11 lat wyjechał do Neapolu i zaczął studiować trivium .
15 czerwca 1565 wstąpił do zakonu dominikanów , przyjmując imię zakonne Giordano ( śluby zakonne złożył w 1566 ) .
Już będąc w seminarium , popadł w konflikt z bezpośrednimi przełożonymi , krytykując podstawowe dogmaty wiary katolickiej .
Mimo to w 1572 przyjął święcenia kapłańskie , a w 1575 uzyskał najwyższy stopień naukowy zatwierdzony dla dominikańskich szkół lector publicus , który uprawniał do publicznego głoszenia wykładów i prowadzenia dysput `` de quodlibet '' ( na wybrany przez uczestników temat ) oraz dawał prawo do honorowego używania tytułu „ doktor teologii ” .
Zajmował się filozofią , okultyzmem i mnemotechniką .
Wędrówka przez Europę .
W lutym 1576 został oskarżony o herezję ( akt oskarżenia przeciwko niemu zawierał blisko 130 punktów ) .
Groził mu zakonny sąd inkwizycyjny prowincji neapolitańskiej , wydalenie z zakonu i ekskomunika .
Aby go uniknąć , uciekł do Rzymu , gdzie schronił się w klasztorze dominikańskim przy Piazza della Minerva ( tu istotna uwaga : w XVI wieku Królestwo Neapolu było w jurysdykcji Hiszpanii ) .
W Rzymie długo nie zagościł .
W kilka dni po przybyciu do Wiecznego Miasta otrzymał list , w którym przyjaciele z Neapolu poinformowali go , że znaleziono zakazane książki , które porzucił , gdy dowiedział się , że został oskarżony i że całe śledztwo w jego sprawie zostało przekazane z Neapolu do Rzymu .
W tej sytuacji postanowił zniknąć z oczu jakiegokolwiek kolegium inkwizycyjnego i potajemnie , nie pozostawiając po sobie żadnego śladu , opuścić Rzym .
Zrzucił habit i powrócił do imienia Filip .
Tak rozpoczęła się jego tułaczka po Europie .
Do 1579 wędrował po Włoszech jako prywatny nauczyciel .
15 kwietnia 1576 roku pojawił się w Genui , ale z powodu sytuacji panującej w mieście ( Genua w tym czasie była pod rządami stronnictwa kalwińskiego ) , przeniósł się do nieodległego Noli ( dzisiejsze Vico Noli ) , gdzie niechętnie , na zlecenie władz miejskich , uczył chłopców czytania i pisania , a miejscowym arystokratom objaśniał zasady swojej kosmogonii .
