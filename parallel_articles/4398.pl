Po krótkiej przerwie , dziewczyny powróciły na scenę , razem z nowym singlem `` The Show '' , który był pierwszą piosenką z ich drugiego albumu .
Nowy singel posiadał , rzadko używaną strukturę , dla piosenki w popowych rytmach .
Na tak zbudowanej ekscytacji i oczekiwaniu , wszedł na listę miejscem drugim .
Kolejny singel `` Love Machine '' także uplasował się na miejscu drugim , jednocześnie stając się drugą grupą ( po Spice Girls ) , która wydała 6 singli w pierwszej trójce w Wielkiej Brytanii .
Singel `` Love Machine '' został nominowany do nagrody `` Piosenka Roku '' , kanału ITV ostatecznie zajął szóste miejsce .
Piosenka została też wykorzystana w reklamie Homebase .
