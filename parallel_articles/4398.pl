2005–2006 : Chemistry .
Po zakończeniu swojej pierwszej trasy koncertowej , dziewczyny wróciły do studia , aby nagrać nowy album .
Pierwszym singlem z `` Chemistry '' została piosenka Long Hot Summer , która dotarła do # 7 miejsca na brytyjskiej liście przebojów .
Kolejnym singlem zostało Biology , które , jak się później okazało , stało się najpopularniejszym singlem z tego albumu , docierając do miejsca czwartego .
5 grudnia miał swoją premierę album `` Chemistry '' .
Album zadebiutował na # 11 miejscu , sprzedając się w całkowitym nakładzie 360 000 egzemplarzy .
Trzecim singlem została piosenka See the Day , która zadebiutowała na miejscu dziewiątym .
Została wydana 19 grudnia 2005 roku .
Czwartym i ostatnim singlem z trzeciego albumu Girls Aloud została piosenka Whole Lotta History , która dotarła do szóstego miejsca brytyjskiej listy przebojów .
2006-2007 : The Sound of Girls Aloud .
We wrześniu 2006 roku oficjalnie potwierdzono , że dziewczyny wydadzą składankę swoich największych przebojów .
Album `` The Sound of Girls Aloud '' został wydany 30 października 2006 roku .
Album został najlepiej sprzedającym się albumem dziewcząt , sprzedając się w Wielkiej Brytanii w ilości 720 000 egzemplarzy .
Pierwszym singlem z albumu została piosenka Something Kinda Ooooh , wydana 16 października 2006 roku .
Piosenka zadebiutowała na miejscu piątym , a po tygodniu dotarła do miejsca trzeciego .
Drugim i ostatnim singlem zostało I Think We 're Alone Now , które dotarło do miejsca czwartego .
