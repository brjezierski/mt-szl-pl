2002–2004 : `` Sound of the Underground '' .
Po ogromnym sukcesie singla Sound of the Underground , nowo uformowana grupa pracowała przez pięć miesięcy nad swym debiutanckim albumem .
Nieco później , w maju 2003 , nagrały hit `` No Good Advice '' .
Piosenka zebrała negatywne oceny .
Same wokalistki oceniły teledysk jako najmniej przez nie lubiany , ze względu na trudności przy jego produkcji .
W tym samym roku tenże wideoklip uznano za jeden z najseksowniejszych .
Debiutowy album `` Sound of the Underground '' został wydany 26 maja 2003 .
Uplasował się na drugiej pozycji tuż za albumem Justina Timberlake ' a `` Justified '' .
Piosenka `` Girls Allowed '' , została napisana dziewczynom przez byłego członka grupy Westlife – Bryana McFaddena .
Współautorem piosenki `` Some Kind of Miracle '' była Edele Lynch .
Trzeci singel `` Life Got Cold '' , wydany 23 sierpnia , zajął trzecie miejsce .
Pomógł on powrócić albumowi do UK Top 20 .
W październiku 2003 roku został wydany cover piosenki Pointer Sisters – `` Jump ( For My Love ) '' .
Piosenka towarzyszyła soundtrackowi do filmu `` Love Actually '' .
Singel uplasował się na miejscu drugim , tuż za piosenką `` Westlife '' – `` Mandy '' .
`` Jump '' pomógł we wzroście sprzedaży reedycji albumu ( wydanego 1 grudnia 2003 ) , który uzyskał miano brytyjskiej `` platynowej płyty '' .
W nowej wersji albumu na miejsce trzech usuniętych piosenek wstawiono nowe : `` Jump ( For My Love ) '' , `` You Freak Me Out '' ( ścieżka dźwiękowa do filmu `` Zakręcony piątek '' ) oraz `` Girls On Film '' ( Cover zespołu Duran Duran , B-Side do singla `` Life Got Cold '' ) .
`` You Freak Me Out '' został wykonany na programie CD : UK na początku roku 2004 .
Prezenter tego kanału ogłosił , że to będzie ich kolejny singel , jednak plany zrobienia klipu zostały porzucone , a dziewczyny rozpoczęły pracę nad kolejnym albumem .
Z powodu gwałtownej obniżki ceny i wysokiej popularności kolekcji `` Greatest Hits '' , w grudniu 2006 `` Sound Of The Underground '' powracając na Top 75 sprzedał się w liczbie 10.000 kopii dając ogólnie , wynik ok. 330.000 kopii , co nie uratowało go przed tytułem `` Najgorzej sprzedającego się albumu Girls Aloud '' .
Uplasował się blisko za `` Chemistry '' – 360.000 i `` What Will the Neighbours Say '' – 570.000 .
Album utrzymał się w Top 75 przez 20 tygodni w okresie ponad trzech lat .
