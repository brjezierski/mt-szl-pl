Girls Aloud
Girls Aloud – brytyjski girlsband stworzony w programie Popstars na kanale ITV1 w edycji The Rivals z 2002 roku .
Dziewczyny zwyciężyły w imprezie Smash Hits oraz zdobyły nagrodę TMF .
Były także nominowane do BRIT Award .
Grupa , składająca się z Cheryl Cole ( z.d .
Tweedy ) , Nadine Coyle , Sarah Harding , Nicola Roberts i Kimberley Walsh , ustanowiła rekord 20 ( wszystkich ) singli w pierwszej dziesiątce ( włączając 4 na pierwszej pozycji ) .
18 lutego 2009 roku zdobyły swoją pierwszą nagrodę na BRIT Awards w kategorii `` Najlepsza piosenka pop '' .
Zdobyły też popularność w Europie hitami # 1 w Grecji czy w Irlandii .
Udowodniły swą urodę , zdobywając Glamour Women of the Year Award oraz FHM Bloke Award .
