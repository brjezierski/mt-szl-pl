2008-2010 : Out of Control .
Piąty studyjny album ( ogółem szósty ) , który wydany został 3 listopada 2008 roku .
W pierwszym tygodniu pobytu na liście UK Albums Chart zajął pierwsze miejsce , co czyni go drugim jak do tej pory albumem Girls Aloud , który osiągnął to miejsce .
Pierwszym singlem promującym album jest piosenka The Promise , która zadebiutowała na miejscu 1. na liście UK Singles Chart ( jest ogółem 4 singlem zespołu , który dotarł do pierwszego miejsca ) .
Singiel miał premierę 20 października 2008 .
W lutym 2009 roku odbyła się gala Brit Awards 2009 na której zespół wykonał ten oto utwór .
Singiel został również nominowany do tytułu Najlepszej Piosenki i zdobył tę zaszczytną nagrodę .
Drugim singlem została piosenka The Loving Kind .
Singiel wydany został 12 stycznia 2009 .
Premiera teledysku w Wielkiej Brytanii odbyła się 3 grudnia na kanale 4Music oraz Channel 4 .
Utwór ten 18 stycznia 2009 roku trafił do TOP 10 na liście UK Singles Chart , co oznacza , że wszystkie wydane do tej pory single znalazły się w TOP 10 w Wielkiej Brytanii .
Trzecim singlem promującym album jest utwór `` Untouchable '' który został uznany za najlepszą piosenkę albumu `` Out of Control '' .
Singiel wydany został 27 kwietnia 2009 roku .
Premiera teledysku odbyła się 25 marca 2009 roku na brytyjskim kanale telewizyjnym `` 4Music '' i `` Channel 4 . ''
Untouchable jest pierwszym singlem zespołu który nie trafił do Top 10 w Wielkiej Brytanii .
Piosenka dotarła do miejsca 11 .
Album promowany był również trasą koncertową Out of Control Tour .
2012 : Powrót .
Po trzech latach przerwy dziewczyny z Girls Aloud ponownie spotkały się w studiu z okazji dziesiątej rocznicy powstania zespołu .
Cheryl Cole potwierdziła powrót zespołu i stwierdziła , że będzie to miało miejsce w listopadzie 2012 roku .
Powrót grupy związany jest z wydaniem charytatywnego singla dla organizacji `` Children in Need '' .
Teledysk do utworu został nakręcony w październiku 2012 roku .
9 października 2012 oficjalna strona zespołu została zaktualizowana o odliczanie do dnia premiery teledysku , a następnie wydaniem krótkiego zwiastunu widea w dniu 15 października ; tego samego dnia singel wyciekł do internetu .
Nowy utwór nosi nazwę `` Something New '' , a jego radiowa premiera odbyła się w rozgłośni Capital FM , w południe 16 października 2012 .
Oficjalnie singel został wydany 18 listopada 2012 .
26 listopada 2012 roku ukazał się album podsumowujący dziesięcioletnią działalność zespołu .
Prócz największych przebojów , wydawnictwo zawiera cztery nowe nagrania , powstałe na potrzeby promocji albumu .
Krążek dotarł do 9 miejsca UK Albums Chart i rozszedł się do marca 2013 w ilości 156,000 egzemplarzy .
Drugim singlem promującym album został utwór `` Beautiful 'Cause You Love Me '' wydany 17 grudnia 2012 roku .
Utwór ten dotarł tylko do 97 miejsca UK Singles Chart i jest pierwszym singlem zespołu , który nie znalazł się w top 20 .
2013 : Zakończenie kariery .
