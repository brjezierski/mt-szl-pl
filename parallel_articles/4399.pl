Girondins Bordeaux
Football Club des Girondins de Bordeaux – francuski klub piłkarski z siedzibą w Bordeaux , występujący w rozgrywkach Ligue 2 .
Historia .
Klub Girondins został założony 1 października 1881 lub 1 lutego 1882 ( źródła podają różne daty ) .
Pierwotna nazwa brzmiała : Societe de Gymnastique et de Tir de Girondins .
Był to klub tak zwany „ omnisport ” , czyli wielosekcyjny ( biegi , szermierka , pływanie… ) .
W 1910 powstała sekcja piłki nożnej , która funkcjonowała jednak tylko jeden rok .
Reaktywowana została w roku 1919 i po fuzji z Argus Sport Bordeaux przyjął kolory , które funkcjonują do dzisiaj .
W 1924 doszło do zmiany nazwy na Girondins Guyenne Sports .
