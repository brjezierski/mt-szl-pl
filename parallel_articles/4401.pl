Konewka
Konewka to przenośny zbiornik na wodę , wykonany najczęściej z blachy lub z tworzywa sztucznego , przeznaczony do podlewania roślin .
Kształt konewki jest zbliżony do walca o podstawie eliptycznej , z półkolistym uchwytem u góry .
Z boku , przy podstawie przymocowana jest końcówka , biegnąca skośnie do góry i służąca do podlewania .
Jej kształt jest zbliżony do mocno wydłużonego ściętego stożka , zakończonego rozpryskiwaczem .
Ma on za zadanie rozdzielić mocny strumień wody , tak aby nie wypłukiwał on gleby i nie tworzył w niej zagłębień .
Główny zbiornik konewki oraz wydłużona końcówka tworzą układ naczyń połączonych .
Podlewanie odbywa się poprzez przechylanie konewki w stronę końcówki , co powoduje podniesienie w niej poziomu wody i powstanie ciśnienia , dzięki któremu woda wypływa przez sitko .
Górna powierzchnia konewki jest zwykle częściowo zamknięta od strony końcówki , tak aby zapobiec wylewaniu się wody przy przechylaniu .
W mniejszych konewkach z tworzyw sztucznych często zamknięta jest cała górna powierzchnia , w której pozostawiony zostaje jedynie mały okrągły otwór do nalewania wody .
Objętość konewek waha się w granicach od kilku do kilkudziesięciu litrów .
