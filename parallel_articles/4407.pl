Giumri ( stacja kolejowa )
Giumri ( arm : `` Գյումրիի Երկաթգծի Կայարան '' ) – stacja kolejowa w Giumri , w Armenii .
Znajdują się tu 2 perony .
Węzeł kolejowy w Giumri jest najstarszym i największym w Armenii .
Powstał w 1897 .
Pierwszą linią kolejową było połączenie z Tbilisi w 1899 roku , a następnie przedłużono je do Erywania ( w 1902 ) , Kars ( w 1902 ) , Dżolfa ( w 1906 roku ) i Tabriz .
W rezultacie , Giumri stał się ważnym węzłem kolejowego .
Od roku 2013 , Dworzec kolejowy Giumri obsługuje regularne połączenia do Erywania i Batumi .
Obecnym operatorem stacji kolejowej jest Południowokaukazka Kolej .
