Jeden z najwybitniejszych kompozytorów operowych w historii .
Życiorys .
Giuseppe Verdi urodził się w wiosce Le Roncole w okolicach Busseto w ówczesnym Księstwie Parmy i Piacenzy .
Gdy Verdi był dzieckiem , rodzina przeprowadziła się do Busseto – to tam Verdi zaczął pobierać pierwsze , nieregularne lekcje muzyki od miejscowego organisty .
Przyjacielem rodziny był miejscowy kupiec i miłośnik muzyki , Antonio Barezzi , który objął Verdiego mecenatem .
W wieku 18 lat Verdi próbował dostać się na studia do konserwatorium w Mediolanie , jednak jego kandydatura została odrzucona z powodu `` niewłaściwej pozycji dłoni podczas grania '' oraz wieku .
Dzięki wsparciu finansowemu Barezziego , Verdi nawiązał kontakty z muzykami La Scali : był obecny na występach oraz pobierał prywatne lekcje od klawesynisty , Vincenzo Lavigna .
W 1836 wrócił do Busseto , gdzie podjął pracę nauczyciela muzyki , tego samego roku ożenił się z córką swojego mentora , Margheritą Barezzi .
Z tego związku urodziło się dwoje dzieci : Virginia ( 1837–1838 ) i Icilio ( 1838–1839 ) .
Z przyczyn finansowych Verdi zdecydował się ponownie przenieść się do Mediolanu .
Po śmierci żony , w 1840 Verdi ukończył swoją drugą operę , `` Un giorno di regno '' , wystawioną w tym samym roku w La Scali .
W 1842 impresario La Scali , Bartolomeo Merelli przedstawił Verdiemu libretto `` Nabucco '' , do którego w krótkim czasie Verdi skomponował muzykę .
Opera odniosła sukces przynosząc Verdiemu popularność , zaś chór z trzeciego aktu , `` Va , pensiero '' stał się nieoficjalnym hymnem Włoch .
W tym samym roku Verdi poznał Giuseppinę Strepponi , z którą ożenił się w 1859 .
W 1843 odbyła się premiera kolejnej opery Verdiego , `` I Lombardi alla Prima Crociata '' , która jednak została ocenzurowana ze względu na możliwą interpretację patriotyczną .
Kolejne dzieła kompozytora , tworzone w latach 1843–1849 również były dobrze przyjmowane przez publikę .
