Giza ( arab .
, `` Al-Ǧīza '' ; egip .
arab .
`` Ig-Gīza '' ) – miasto w Egipcie , na zachodnim brzegu Nilu , naprzeciw Kairu .
W 2006 roku liczyło ok. 3,1 mln mieszkańców , co czyni je trzecim pod względem liczby ludności miastem w kraju .
Wchodzi w skład aglomeracji Kairu .
Giza znana jest jako miejsce jednych z najbardziej imponujących budowli starożytności , powstałych na tym terenie w większości w XXV w .
p.n.e. , przy czym słynne piramidy w Gizie oddalone są o ok. 8 km od centrum starożytnej Gizy .
Kompleks piramid oraz pozostałości państwa były atrakcją turystyczną już w starożytności , kiedy niektóre z budowli liczyły 2000 lat .
