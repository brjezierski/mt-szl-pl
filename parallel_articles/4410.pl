Opis fabuły .
Rok 1913 , trwa rewolucja meksykańska .
Na pustkowiu krzyżują się ścieżki Juana Mirandy ( Rod Steiger ) , rzezimieszka dowodzącego rodzinną szajką i Irlandczyka Johna Mallory'ego ( James Coburn ) , członka IRA zbiegłego ze swego kraju w obawie przed aresztowaniem przez Brytyjczyków .
Meksykanin od lat planuje napad na bank w pobliskim mieście Mesa Verde , a Mallory – specjalista od materiałów wybuchowych – posiada umiejętności ułatwiające rozbicie sejfu ze złotem .
Miranda próbuje namówić Mallory'ego do zawarcia specyficznej `` spółki '' , prośbą i groźbą .
Ten – ku niekłamanemu zdziwieniu bandyty – ostatecznie ulega .
Razem organizują i przeprowadzają udany napad na gmach bankowy , jednak w jego podziemiach zamiast pełnego skarbca Miranda znajduje więzionych rewolucjonistów .
Okazuje się , że jego akcja była częścią planu wymierzonego przeciw wojskowej dyktaturze , a on sam – wbrew swej woli – zostaje obwołany ludowym bohaterem .
Chcąc nie chcąc , bandyta przyłącza się do rewolucji , za co przyjdzie mu zapłacić rodzinną tragedią – ścigające rewolucjonistów wojsko wymorduje wszystkich jego synów .
