Giżycko
Giżycko ( , pol .
( dawniej ) `` Łuczany , Lec '' , prus . )
– miasto w województwie warmińsko-mazurskim , siedziba powiatu giżyckiego i gminy wiejskiej Giżycko .
Miasto jest położone pomiędzy jeziorami Kisajno i Niegocin .
Giżycko jest nazywane stolicą żeglarstwa w Polsce .
Jest jednym z głównych portów na szlaku wielkich jezior mazurskich i jednym z polskich ośrodków turystycznych i wypoczynkowych .
Według danych GUS w 2016 r. ludność Giżycka wynosiła 29 642 osoby .
Giżycko uzyskało lokację miejską w 1612 roku .
Położenie .
Giżycko jest położone w centralnej części Pojezierza Mazurskiego w Krainie Wielkich Jezior Mazurskich .
Pod względem administracyjnym położone jest we wschodniej części województwa warmińsko-mazurskiego , na północny wschód od Olsztyna .
