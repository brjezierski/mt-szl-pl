Gjirokastёr , Gjirokastra ( gr .
Αργυρόκαστρο , `` Arjirókastro '' , pol .
rzadko : `` Dżirokastra '' ) – miasto w południowej Albanii w okręgu Gjirokastёr .
Miasto jest jednym z dwóch albańskich ( oprócz Beratu ) miast-muzeów .
Zawdzięcza to między innymi kamiennym domkom starówki , które są pokryte szarymi łupkami .
Liczne uliczki są bardzo strome , dlatego też Gjirokastёr zwana jest `` Miastem Tysiąca Schodów '' .
W 2005 roku miasto zostało wpisane na Listę Światowego Dziedzictwa UNESCO .
W 2008 roku wpis rozszerzono o miasto Berat .
Nazwa `` Gjirokastёr '' wywodzi się z języka greckiego .
Za czasów panowania bizantyńskiego , w XIII w. miasto nosiło nazwę `` Argyropolis '' ( „ Srebrne Miasto ” ) lub `` Argyrokastron '' ( „ Srebrna Twierdza ” ) .
Sami Albańczycy uważają , iż właściwe wyjaśnienie nazwy wiązać należy ze starożytnym iliryjskim plemieniem Argyrów , które w starożytności zamieszkiwało te tereny .
Nad miastem góruje stara , średniowieczna twierdza , rozbudowana przez Turków osmańskich .
Niegdyś w podziemiach twierdzy mieściło się więzienie dla więźniów politycznych .
Gjirokastёr jest rodzinnym miastem najbardziej znanego na świecie pisarza albańskiego – Ismaila Kadare .
W mieście tym rozgrywa się akcja kilku jego powieści , w tym `` Kronika ne gurë '' ( `` Kronika w kamieniu '' ) .
Urodził się tutaj również Enver Hoxha , przywódca komunistycznej Albanii w latach 1944-85 .
