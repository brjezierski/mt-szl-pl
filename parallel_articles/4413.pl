Gladbeck
Gladbeck − miasto w Niemczech w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Münster , w powiecie Recklinghausen .
Powierzchnia miasta wynosi 35,91 km² i zamieszkiwana jest przez 75 253 osoby ( 31.12.2010 ) .
Średnia wysokość to 52 m n.p.m .
Gospodarka .
W mieście rozwinął się przemysł elektrotechniczny , metalowy , chemiczny , precyzyjny oraz odzieżowy .
