Mieczyk ( akwarystyka )
Mieczyk , mieczonosz – potoczna nazwa kilku gatunków ryb akwariowych .
Początkowo nazwą tą określano `` Xiphophorus helleri '' , później inne gatunki piękniczkowatych charakteryzujące się obecnością mieczowatego wyrostka na płetwie ogonowej .
