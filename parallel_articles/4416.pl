Papier ścierny
Papier ścierny – jeden z rodzajów wyrobów ściernych przeznaczonych do obróbki ściernej powierzchni przedmiotów wykonanych z takich materiałów jak drewno , metal , tworzywa sztuczne .
W handlu występuje zwykle w postaci arkuszy ( formatu A4 ) lub taśm grubszego papieru , na którego jednej ze stron znajduje się tzw .
nasyp – czyli warstwa ziarna ściernego przyklejonego do podłoża .
Jako materiał ścierny dawniej stosowano drobno tłuczone szkło – stąd czasem zamiennie używana nazwa „ papier szklisty ” .
Dziś takim materiałem jest zwykle korund ( Al2O3 ) lub karborund ( SiC ) .
Oznaczenie ziarnistości .
Rozróżnia się papiery grubo- i drobnoziarniste .
Oznaczenie ziarnistości znajduje się na spodzie arkusza ( taśmy ) w postaci liczby , np .
30 , 60 , 120 , 360 , itp .
Liczba ta jest związana z wielkością ziaren materiału ściernego – im wyższa liczba w oznaczeniu , tym drobniejsze ziarno użyte do produkcji papieru .
Oznacza liczbę oczek w sicie na jeden cal kwadratowy użytym do przesiania materiału ( przy gradacjach powyżej 240 – prowadzi się analizę metodą sedymentacji ) ( europejska norma ISO 6344 ) .
W zależności od rodzaju papieru i użytego kleju papiery ścierne dzieli się na zwykłe i wodoodporne .
Tych drugich ( zwykle o gradacji powyżej 300 do 2500 ) po zwilżeniu wodą lub naftą używa się do szlifowania powierzchni .
Historia .
Pierwsza wzmianka o papierze ściernym należy do XIII wieku , kiedy w Chinach był produkowany papier ścierny stworzony z mielonej muszli , nasion i piasku , klejonych do pergaminu z pomocą naturalnego kleju .
U niektórych narodów zamiast papieru ściernego była używana skóra rekina .
Uważa się , że wynalazcą nowoczesnego papieru ściernego jest John Oakey ( 1813–1887 ) – angielski wynalazca i założyciel firmy „ John Oakey i synowie ” , producent papieru ściernego i innych materiałów szlifierskich .
