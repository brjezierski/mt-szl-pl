Bombka
Bombka , bańka ( w niektórych regionach Polski m.in .
w Małopolsce ) – ozdoba choinkowa , wykonana zazwyczaj z cienkiego szkła .
Bombki często są ręcznie malowane i posrebrzane od wewnątrz .
Początki ich produkcji datuje się na 1847 .
Pierwsze bombki zostały wykonane w mieście Lauscha w Niemczech przez Hansa Greinera .
Według legendy rozpoczął on tworzenie szklanych wydmuszek do udekorowania choinki , gdyż nie stać go było na typowe wówczas dekoracje czyli orzechy , jabłka czy cukierki .
Pierwsze choinkowe bombki pojawiły się w Polsce w XIX w. Były wykonywane ze szkła dmuchanego i początkowo imitowały orzechy i inne owoce , ale z czasem wzornictwo wzbogacało się o bombki w kształcie przedmiotów codziennego użytku , np .
bucików , parasolek , instrumentów muzycznych czy też postaci zwierzątek i pajaców .
Zabawki choinkowe pierwotnie sprowadzano z zagranicy , a ich prawdziwym zagłębiem była Norymberga .
Ozdoby stamtąd były w Polsce bardzo popularne .
Polska to potentat w produkcji ręcznie robionych bombek .
Niemal cała produkcja trafia na eksport , głównie do USA , ale także do Japonii , Kanady , Wielkiej Brytanii , Francji , Niemiec , Rosji , Skandynawii , Australii i innych krajów .
