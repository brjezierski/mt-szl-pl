Rękawiczki
Rękawiczki – zaliczane do galanterii okrycie dłoni z jednym lub pięcioma palcami , z różnej długości mankietem zakrywającym przedramię , noszone dla ozdoby , ochrony przed chłodem , brudzącymi lub szkodliwymi substancjami .
Historia rękawiczek .
Rękawice znano już w starożytnym Egipcie , Grecji i Rzymie , gdzie stosowano je dla ochrony przed zimnem , ubrudzeniem rąk , przed skaleczeniem przy pracy .
Podczas prac polowych noszono rękawice w formie woreczków .
Grecy i Rzymianie nakładali na palce osłonki także podczas jedzenia ( jedzono palcami ) .
Chrześcijanie podczas sprawowania liturgii stosowali rękawiczki już w średniowieczu .
Stały się one symbolem godności i władzy , najpierw biskupiej , potem także świeckiej .
Metalowe rękawice stosowano do zbroi rycerskiej .
Około 1000 roku rękawice pojawiły się w stroju kobiecym .
Wyrabiane je ze skóry , także futerkowej , z tkanin , filcu , a w XIII wieku pojawiły się pierwsze dziane rękawice .
W XVIII wieku stały się bardzo popularne , początkowo używały je tylko zamożniejsze niewiasty .
Charakterystyczne dla tego okresu są zwłaszcza długie rękawice , zakrywające całe ręce do sukni z krótkimi rękawami .
Takie rękawiczki później zakładano do sukni balowych .
Występowały one w postaci pięciopalczastej lub jako tzw .
