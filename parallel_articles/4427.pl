Gliny ( serial telewizyjny )
Gliny ( tytuł org .
`` Popoz '' , od 2013 ) – holenderski serial komediowy w reżyserii Martijna Smitsa i Erwina van den Eshofa .
Wyprodukowany przez Alain de Levita .
Premiera serialu miała miejsce w Holandii 3 listopada 2013 roku na holenderskim kanale Comedy Central .
W Polsce serial emitowany od 7 lutego 2014 roku na antenie Comedy Central Polska .
Opis fabuły .
Serial opowiada o losach dwóch detektywów – Iviego i Randy'ego oraz ich stażyście Coco ( ginie w pierwszym epizodzie ) , którzy walczą ze złem w bardzo nieprzewidywalny i często komiczny sposób .
Mimo że są twardzielami , biorą narkotyki , a także nie wahają się użyć broni .
Bohaterowie przeżywają wspólnie niesamowite przygody .
