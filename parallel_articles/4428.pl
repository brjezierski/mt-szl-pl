Diecezja gliwicka
Diecezja gliwicka ( łac .
`` Dioecesis Glivicensis '' ) – jedna z 3 diecezji obrządku łacińskiego w metropolii katowickiej w Polsce ustanowiona 25 marca 1992 przez papieża Jana Pawła II bullą `` Totus Tuus Poloniae populus '' .
Najważniejsze miasta : Gliwice , Bytom , Lubliniec , Tarnowskie Góry , Zabrze .
W 2021 diecezja składała się ze 156 parafii ( w tym 18 zakonnych ) , w których pracowało 335 księży .
W seminarium przebywało 19 kleryków .
Udzielono 5091 chrztów ( wobec 5121 rok wcześniej ) , 6030 I komunii ( wobec 5509 rok wcześniej ) , 4250 bierzmowań ( 3831 w 2020 ) i 1441 śluby ( 1213 w 2020 ) .
Na niedzielną mszę uczęszczało 26,7 % wiernych , do komunii przystąpiło 13,4 % , a 80,6 % dzieci uczęszczało na religię .
