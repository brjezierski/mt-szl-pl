Gloeodes pomigena
Gloeodes pomigena ( Schwein . )
Colby – gatunek workowców .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gloeodes , Incertae sedis , Incertae sedis , Incertae sedis , Incertae sedis , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1832 r. Lewis Schweinitz nadając mu nazwę `` Dothidea pomigena '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu 1920 r. Arthur Samuel Colby .
Synonimy :
Znana jest tylko anamorfa .
Dla teleomorfy nie ustalono jej pozycji taksonomicznej .
Zgodnie z przyjętymi dla grzybów zasadami nazewnictwa w takim przypadku takson ten klasyfikuje się w obrębie workowców ( dawniej zaliczany był do grzybów niedoskonałych ) .
Występowanie .
Gatunek ten opisano na jabłoni domowej jako sprawcę choroby zwanej brudną plamistością jabłek .
Podczas badań naukowych przeprowadzonych przez dr Ewę Mirzwę-Mróz w 2015 r. w Samodzielnym Zakładzie Fitopatologii SGGW w Warszawie na jabłkach porażonych przez tę chorobę nie znaleziono jednak `` Gloeodes pomigena '' , odkryto natomiast 5 innych gatunków grzybów będących jej sprawcami ) .
`` Gloeodes pomigena '' znajduje się w wykazie grzybów mikroskopijnych Polski .
Jako sprawcę brudnej plamistości jabłek opisał go W. Siemaszko w 1937 r. W związku z badaniami dr E. Mirzwy-Mróz jego występowanie w mykobiocie Polski wymaga jednak potwierdzenia .
