Niszczyca belkowa
Niszczyca belkowa ( `` Gloeophyllum trabeum '' ( Pers . )
Murrill ) – gatunek grzybów z rodziny niszczycowatych ( `` Gloeophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gloeophyllum , Gloeophyllaceae , Gloeophyllales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Agaricus trabeus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1908 r. William Alphonso Murrill .
