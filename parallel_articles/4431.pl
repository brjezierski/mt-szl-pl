Glomus macrocarpum
Glomus macrocarpum Tul .
& amp ; C. Tul .
– gatunek grzybów należący do rodziny `` Glomeraceae '' , typ nomenklatoryczny rodzaju `` Glomus '' .
Nazwa gatunkowa pochodzi od greckiego słowa `` macrocarpus '' , znaczącego `` wielkoowocnikowy '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Glomus , Glomeraceae , Glomerales , Incertae sedis , Glomeromycetes , Incertae sedis , Glomeromycota , Fungi '' .
Synonimy :
Charakterystyka .
Żyje w glebie , tworząc mikoryzę endotroficzną z korzeniami roślin .
Wytwarza owocniki o pokroju okrągławym , elipsoidalnym o średnicy do 1,2 cm , zawierające 2–15 zarodników .
Zarodniki mają postać żółtych , okrągławych chlamydospor o średnicy ok. 110–120 µm , czasami mają pokrój eliptyczny lub gruszkowaty o zbliżonych wymiarach , nie wybarwiają się pod wpływem odczynnika Melzera .
