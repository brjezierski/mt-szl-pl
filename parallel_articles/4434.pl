Glew ( prowincja Buenos Aires )
Glew – miasto w Argentynie , w prowincji Buenos Aires .
Drugie pod względem liczby ludności miasto w Partido Almirante Brown .
Jest częścią aglomeracji `` Gran Buenos Aires '' .
Znajduje się 33 km od Buenos Aires i 54 km od stolicy prowincji miasta La Plata .
W 2010 r. miasto zamieszkiwało 57,8 tys .
mieszkańców .
Miejscowość została założona w 1888 r. pod nazwą `` Pueblo Cambáceres '' .
W 1957 r. miejscowość otrzymała prawa miejskie , jako Glew .
Nazwa miasta pochodzi od nazwiska Juana Glewa , który był właścicielem tych ziem w XIX wieku .
