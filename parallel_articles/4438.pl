Bielany ( rejon brasławski )
Bielany – dawna kolonia .
Tereny , na których była położona , leżą obecnie na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Mieżany .
Na mapie WIG występuje pod nazwą Bielanie .
Historia .
W latach 1921–1939 wieś a następnie kolonia leżała w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie brasławskim , w gminie Brasław .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 67 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Było tu 10 budynków mieszkalnych .
Kolonia należała do parafii rzymskokatolickiej w m. Mieżany .
W 1933 podlegała pod Sąd Grodzki w Brasławiu i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Brasławiu .
