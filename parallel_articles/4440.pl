Chiny
Chiny – region historyczno-kulturowy w Azji , będący jedną z najstarszych cywilizacji na świecie .
Zamieszkany przez wiele narodów i grup etnicznych , z których największą są Chińczycy Han .
W węższym , potocznym znaczeniu termin „ Chiny ” współcześnie odnoszony jest do Chińskiej Republiki Ludowej .
Republika Chińska nie stanowi skróconej nazwy „ Chińskiej Republiki Ludowej ” .
Republika Chińska ( Tajwan ) to państwo nieuznawane przez większość społeczności międzynarodowej , w wielu organizacjach międzynarodowych zwane „ Chińskim Tajpej ” .
Licząca ponad 5000 lat chińska cywilizacja jest ojczyzną wielu wynalazków , m.in .
prochu , papieru , czy kompasu .
Wywarła znaczny wpływ na kulturę krajów sąsiednich : Korei , Japonii , Wietnamu i Mongolii .
Chiny są też ojczyzną bogatej tradycji muzycznej , teatralnej , literackiej i filozoficznej .
