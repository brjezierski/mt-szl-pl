Gmina Danderyd
Gmina Danderyd ( szw .
`` Danderyds kommun '' ) – gmina w Szwecji , w regionie Sztokholm , z siedzibą w Djursholm .
Pod względem zaludnienia Danderyd jest 80. gminą w Szwecji .
Zamieszkuje ją 30 100 osób , z czego 51,74 % to kobiety ( 15 574 ) i 48,26 % to mężczyźni ( 14 526 ) .
W gminie zameldowanych jest 1980 cudzoziemców .
Na każdy kilometr kwadratowy przypada 1075 mieszkańców .
Pod względem wielkości gmina zajmuje 286. miejsce .
