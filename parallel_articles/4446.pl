Gmina Gentofte
Gmina Gentofte ( duń .
`` Gentofte Kommune '' ) jest jedną z gmin w Danii w regionie stołecznym ( do 2007 r. w okręgu Kopenhagi ( Københavns Amt ) ) .
Siedzibą władz gminy jest Charlottenlund .
Gmina Gentofte została utworzona 1 kwietnia 1970 na mocy reformy podziału administracyjnego Danii .
Kolejna reforma w 2007 potwierdziła status gminy .
W Gentofte urodzili się Lars Ulrich , duński perkusista i jeden z założycieli zespołu Metallica oraz Clara Tauson , duńska tenisistka , mistrzyni juniorskiego Australian Open 2019 w grze singlowej .
