Gmina Härnösand
Gmina Härnösand ( szw .
`` Härnösands kommun '' ) – gmina w Szwecji , w regionie Västernorrland , z siedzibą w Härnösand .
Pod względem zaludnienia Härnösand jest 98. gminą w Szwecji .
Zamieszkuje ją 25 273 osób , z czego 50,99 % to kobiety ( 12 886 ) i 49,01 % to mężczyźni ( 12 387 ) .
W gminie zameldowanych jest 812 cudzoziemców .
Na każdy kilometr kwadratowy przypada 23,74 mieszkańca .
Pod względem wielkości gmina zajmuje 96. miejsce .
