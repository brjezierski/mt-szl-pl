Jaśliska
Jaśliska – wieś ( dawniej miasto ) w Polsce położona w województwie podkarpackim , w powiecie krośnieńskim , w gminie Jaśliska .
Leży w Beskidzie Niskim nad rzeką Jasiołką przy ujściu potoku Bełcza ( też : Bielcza ) na wzgórzu , przy drodze wojewódzkiej nr 897 .
Jaśliska uzyskały lokację miejską w 1365 roku , zdegradowane w 1896 roku .
Miasto biskupstwa przemyskiego w powiecie bieckim województwa krakowskiego w końcu XVI wieku .
W latach 1934-54 i 1973–1976 oraz od 2010 siedziba gminy Jaśliska ( przed 2010 w granicach gminy Dukla ) .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Jaśliska .
W latach 1975–1998 miejscowość położona była w województwie krośnieńskim
We wsi liczącej 455 mieszkańców jest czynna restauracja , 2 sklepy spożywcze , sklep przemysłowy , poczta , ośrodek zdrowia oraz schronisko turystyczne .
Miejscowość jest siedzibą rzymskokatolickiej parafii św .
Katarzyny Aleksandryjskiej , która jest celem pielgrzymek do cudownego obrazu Matki Boskiej Jaśliskiej koronowanego w 1997 przez Jana Pawła II .
Zabytkowy zespół zabudowy drewnianej z zachowanym układem urbanistycznym dawnego miasteczka włączono do podkarpackiego Szlaku Architektury Drewnianej .
Historia .
Jaśliska są dawnym obronnym miasteczkiem przygranicznym , które strzegło drogi na trasie z południa Europy ( szczególnie z Węgier : Buda – Miszkolc – Koszyce – Preszów – Stropkov – Przełęcz Beskid nad Czeremchą – Jaśliska – Przełęcz Szklarska – Rymanów – Sanok , oraz do Dukli i Krosna ) .
Świadczą o tym pozostałości murów obronnych .
Miasto lokowane na prawie magdeburskim 28 stycznia 1366 roku przez króla Kazimierza Wielkiego na trakcie handlowym prowadzącym na Węgry , pierwotnie pod nazwą `` Hohenstath '' ( `` Wysokie Miasto '' ) .
Dzierżawił je Jan Hanzelino , który dzięki przywilejowi królewskiemu w 1367 ponownie lokował miasto .
