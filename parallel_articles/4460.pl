Gmina Lidingö
Gmina miejska Lidingö ( także `` miasto Lidingö '' ; szw .
`` Lidingö kommun '' lub `` Lidingö stad '' ) – gmina w Szwecji , w regionie Sztokholm , z siedzibą w Lidingö .
Pod względem zaludnienia Lidingö jest 49. gminą w Szwecji .
Zamieszkuje ją 41 407 osób , z czego 52,17 % to kobiety ( 21 600 ) i 47,83 % to mężczyźni ( 19 807 ) .
W gminie zameldowanych jest 2551 cudzoziemców .
Na każdy kilometr kwadratowy przypada 1380,23 mieszkańca .
Pod względem wielkości gmina zajmuje 285. miejsce .
