Gmina Olofström
Gmina Olofström ( szw .
`` Olofströms kommun '' ) – gmina w Szwecji , w regionie Blekinge , siedzibą jej władz jest Olofström .
Pod względem zaludnienia Olofström jest 163. gminą w Szwecji .
Zamieszkuje ją 13 524 osób , z czego 49,47 % to kobiety ( 6690 ) i 50,53 % to mężczyźni ( 6834 ) .
W gminie zameldowanych jest 865 cudzoziemców .
Na każdy kilometr kwadratowy przypada 34,59 mieszkańca .
Pod względem wielkości gmina zajmuje 205. miejsce .
