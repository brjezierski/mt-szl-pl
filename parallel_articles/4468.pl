Gmina Skinnskatteberg
Gmina Skinnskatteberg ( szw .
`` Skinnskattebergs kommun '' ) – gmina w Szwecji , w regionie Västmanland , z siedzibą w Skinnskatteberg .
Pod względem zaludnienia Skinnskatteberg jest 279. gminą w Szwecji .
Zamieszkuje ją 4829 osób , z czego 49,02 % to kobiety ( 2367 ) i 50,98 % to mężczyźni ( 2462 ) .
W gminie zameldowanych jest 326 cudzoziemców .
Na każdy kilometr kwadratowy przypada 7,29 mieszkańca .
Pod względem wielkości gmina zajmuje 147. miejsce .
