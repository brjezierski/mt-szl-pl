Snagov ( wyspa )
Snagov – wyspa na jeziorze Snagov w południowej Rumunii .
Leży w północnej , najszerszej części jeziora , ok. 120–140 m od jego brzegów .
Ma kształt wydłużony w osi pn.-wsch .
– pd.-zach .
o wymiarach maksymalnych 390 x 170 m. Jest niska , płaska , wzdłuż brzegów obrośnięta pasem drzew .
Toń wodna przy brzegach częściowo zarośnięta rozległymi płatami roślin pływających .
Pierwotnie dostęp do wyspy był jedynie droga wodną , obecnie łączy ją z brzegiem ( z miejscowością Siliştea Snagovului ) most .
Już prawdopodobnie w końcu XIV w. z inicjatywy hospodara wołoskiego Mirczy Starego na wyspie powstały pierwsze zabudowania monasteru Snagov , który w następnych dziesięcioleciach bywał wielokrotnie schronieniem kolejnych władców Wołoszczyzny w chwilach walk wewnętrznych i wojen z sąsiadami .
Z czasem klasztor stał się znaczącym ośrodkiem kultury w tej części Bałkanów .
Przez pewien czas wyspa była również miejscem zesłania Konstantyna Kantakuzena ( 1639-1716 ) – bojara , a jednocześnie historyka i pisarza , czołowego wołoskiego humanisty , ojca hospodara Stefana II .
