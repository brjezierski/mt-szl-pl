Gmina Sund
Gmina Sund ( ) – norweska gmina leżąca w regionie Hordaland .
Jej siedzibą jest miasto Skogsvåg .
Sund jest 388. norweską gminą pod względem powierzchni .
Demografia .
Według danych z roku 2005 gminę zamieszkuje 5537 osób .
Gęstość zaludnienia wynosi 55,83 os./km² .
Pod względem zaludnienia Sund zajmuje 180. miejsce wśród norweskich gmin .
