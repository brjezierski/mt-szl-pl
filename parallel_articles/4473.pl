Gmina Tibro
Gmina Tibro ( szw .
`` Tibros kommun '' ) – gmina w Szwecji , w regionie Västra Götaland , z siedzibą w Tibro .
Pod względem zaludnienia Tibro jest 204. gminą w Szwecji .
Zamieszkuje ją 10 572 osób , z czego 49,68 % to kobiety ( 5252 ) i 50,32 % to mężczyźni ( 5320 ) .
W gminie zameldowanych jest 324 cudzoziemców .
Na każdy kilometr kwadratowy przypada 48,04 mieszkańca .
Pod względem wielkości gmina zajmuje 240. miejsce .
