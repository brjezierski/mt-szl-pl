Gmina Vilhelmina
Gmina Vilhelmina ( szw .
`` Vilhelmina kommun '' ) – gmina w Szwecji , w regionie Västerbotten , z siedzibą w Vilhelmina .
Pod względem zaludnienia Vilhelmina jest 249. gminą w Szwecji .
Zamieszkuje ją 7422 osób , z czego 48,8 % to kobiety ( 3622 ) i 51,2 % to mężczyźni ( 3800 ) .
W gminie zameldowanych jest 78 cudzoziemców .
Na każdy kilometr kwadratowy przypada 0,84 mieszkańca .
Pod względem wielkości gmina zajmuje 7. miejsce .
