Ślemień ( gromada )
Ślemień – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Ślemień z siedzibą GRN w Ślemieniu utworzono – jako jedną z 8759 gromad na obszarze Polski – w powiecie żywieckim w woj .
krakowskim , na mocy uchwały nr 32/IV/54 WRN w Krakowie z dnia 6 października 1954 .
W skład jednostki weszły obszary dotychczasowych gromad Ślemień , Kocoń i Las ze zniesionej gminy Ślemień w tymże powiecie .
Dla gromady ustalono 27 członków gromadzkiej rady narodowej .
29 marca 1956 do gromady Ślemień przyłączono przysiółek Skolarówka z gromady Kocierz .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 reaktywowano gminę Ślemień .
