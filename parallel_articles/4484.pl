Postira
Postira , także `` Postira '' – wieś w Chorwacji , w żupanii splicko-dalmatyńskiej , siedziba gminy Postira .
W 2011 roku liczyła 1429 mieszkańców , a w 2021 roku liczba ta wzrosła do 1618 .
Charakterystyka .
Postira jest położona w północnej części wyspy Brač .
W miejscowości funkcjonuje port .
Miejscowa gospodarka opiera się na turystyce , rolnictwie oraz rybołówstwie i przetwórstwie rybnym .
Kultywowane jest tu także rzemiosło ( meblarstwo , mydlarstwo , kamieniarstwo ) .
Nazwa miejscowości została po raz pierwszy wymieniona w dokumentach z Dolu z 1337 roku .
Sama osada powstała na początku XVI wieku – osadnikami byli dotychczasowi mieszkańcy Dolu oraz uchodźcy z terenów osmańskich .
Pod koniec XVI wieku miejscowość przekształcono w twierdzę w związku z osmańskim zagrożeniem .
