Morfologia i rozwój .
Anamorfa rozwija się od wiosny do jesieni jako pasożyt w porażonych tkankach orzecha włoskiego ( `` Juglans regia '' ) .
Acerwulusy o średnicy 70-200 μm tworzą się między skórką i naskórkiem żywiciela .
Powstają w nich sierpowato zagięte , dwukomórkowe , bezbarwne , gładkie konidia o rozmiarach 18–24 × 3–4 μm .
Czasami tworzą się także cylindryczne , jednokomórkowe mikrokonidia o rozmiarach 6-12 × 1 μm .
Teleomorfa rozwija się jako saprotrof na opadłych liściach orzecha włoskiego .
Wiosną wytworzone w niej zarodniki płciowe dokonują infekcji pierwotnej , porażając liście , owoce i młode pędy orzecha włoskiego .
Tworzy kuliste , czarne owocniki typu perytecjum , o średnicy 170–300 μm z prostą szyjką o długości 150–250 μm i średnicy około 50 μm .
Perydium o grubości 15–30 μm zbudowane jest z 4–5 warstw komórek .
Worki 8–zarodnikowe , o rozmiarach 45–60 × 8–14 μm .
Askospory o rozmiarach 19–23 × 3,5 μm , proste , wąskoelipsoidalne lub wrzecionowate .
