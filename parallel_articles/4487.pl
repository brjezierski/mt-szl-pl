Ophiognomonia leptostyla
Ophiognomonia leptostyla ( Fr . )
Sogonov – gatunek grzybów z klasy `` Sordariomycetes '' .
Grzyb mikroskopijny , patogen wywołujący chorobę o nazwie antraknoza orzecha włoskiego .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ophiognomonia , Gnomoniaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fung '' .
Po raz pierwszy takson ten zdiagnozował w 1823 roku Elias Fries nadając mu nazwę `` Sphaeria leptostyla '' .
W oparciu o badania z zakresu biologii molekularnej w 2008 roku Mikhail V. Sogonov przeniósł go do rodzaju `` Ophiognomonia '' .
