Godfrey Thoma
Godfrey Awaire Thoma ( ur .
22 stycznia 1957 ) – nauruański polityk i działacz sportowy , przewodniczący Parlamentu Nauru w 2003 , 2010 oraz w 2013 .
Po raz pierwszy dostał się do parlamentu w 1995 .
Reelekcję uzyskiwał w każdych kolejnych wyborach aż do 2007 , kiedy to znalazł poza Radą Ustawodawczą .
W wyborach z kwietnia 2008 i 2010 , a także z czerwca 2010 ponownie uzyskał mandat deputowanego , jednak w 2013 roku , ponownie znalazł się poza izbą ( jego miejsce zajął Aaron Cook ) .
6 maja 2003 został wybrany przewodniczącym parlamentu , jednak już dzień później zrezygnował ze stanowiska .
Był ministrem w gabinetach René Harrisa ( sprawiedliwość i zasoby morskie ) i Ludwiga Scotty'ego ( sprawiedliwość , rybołówstwo i zasoby naturalne , imigracja i osadnictwo , sport ) .
W parlamencie XVIII kadencji ( 2008 - 2010 ) pełnił funkcję przewodniczącego Komitetu Przywilejów .
13 maja 2010 w szóstej turze głosowań został ponownie wybrany przewodniczącym Rady Ustawodawczej .
18 maja 2010 zrezygnował , pragnąc zapobiec sformowaniu rządu przez Marcusa Stephena .
W utworzonym w połowie listopada 2011 rządzie Sprenta Dabwido objął funkcję ministra transportu , odpowiedzialnego również za Republic of Nauru Phosphate Company .
25 kwietnia 2013 po raz trzeci objął stanowisko przewodniczącego parlamentu .
Działa w Nauruańskiej Federacji Tenisa Stołowego .
