Język a-pucikwar
Język a-pucikwar ( inne nazwy : pucikwar , puchikwar ) – język wymarły używany niegdyś przez rdzenną ludność Andamanów ( należących do Indii ) .
Należał do rodziny wielkoandamańskiej .
Ludność , posługująca się tym językiem , zamieszkiwała południowe wybrzeża Andamanu Środkowego , północno-wschodnie wybrzeża Andamanu Południowego oraz wyspę Baratang .
