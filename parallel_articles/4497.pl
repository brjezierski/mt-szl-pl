Język godoberyjski
Język godoberyjski ( ros .
годоберинский язык ) – jeden z niewielkich języków kaukaskich , używany przez Godoberów .
Należy do języków andyjskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Jest blisko spokrewniony z językiem botlichyjskim .
Dane , mówiące o liczbie osób posługujących się tym językiem są bardzo rozbieżne i niepewne , według statystyk w 1926 r. języka tego używało ok. 2 tys .
osób .
W późniejszym czasie Godoberowie uznawani byli za odłam Awarów i tak też zapisywani podczas kolejnych spisów powszechnych .
Szacunki z roku 1996 mówiły o 2,5–3 tys .
