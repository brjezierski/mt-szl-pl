Godów ( Radom )
Godów – dzielnica Radomia , dawniej wieś .
Najbardziej na południe wysunięta dzielnica miasta .
Prywatna wieś szlachecka , położona była w drugiej połowie XVI wieku w powiecie radomskim województwa sandomierskiego .
Godów to osiedle domów jednorodzinnych , zarówno starych , nawet przedwojennych , pozostałych po osiedlonych tu Niemcach .
Wzdłuż ulic : Drzymały , Wilczyńskiego , Opolskiej powstaje nowoczesne osiedle domów jednorodzinnych .
Znajduje się tam też boisko sportowe na którym gra lokalny zespół Centrum Radom .
Przez dzielnicę przebiega Obwodnica Południowa Radomia , która została otwarta dla ruchu w 2015 .
Na rogu ulic Ciborowskiej i Wiejskiej , zaraz za końcowym przystankiem autobusu miejskiego linii 6 , znajduje się zaniedbany cmentarz , na którym na przełomie wieku XIX i XX , chowano osiedlonych w Radomiu Niemców , Austriaków i Holendrów .
Właścicielem cmentarza jest od stycznia 2009 radomska parafia ewangelicko-augsburska , która zamierza przeprowadzić ekshumację i przenieść szczątki pochowanych tam ludzi do zbiorowego grobu na cmentarzu przy ulicy Kieleckiej .
Godów jako jedyna dzielnica w Radomiu posiada monografię „ Godów .
Dzieje radomskiej dzielnicy ” napisaną przez dr Elżbietę Orzechowską .
Przez osiedle przepływa Strumień Godowski .
