Gogolin ( stacja kolejowa )
Gogolin – stacja kolejowa w Gogolinie na trasie z Opola do Kędzierzyna-Koźla .
Na stacji rozpoczynała się również linia Gogolin – Prudnik , jednak od powodzi w 1997 roku , kiedy został zerwany most na Odrze , była ona przejezdna od tej strony jedynie do stacji Krapkowice Otmęt i w efekcie bezużyteczna .
W roku 2017 stacja obsługiwała 500–699 pasażerów na dobę .
W roku 2020 było to 300-499 pasażerów .
