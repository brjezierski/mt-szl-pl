Gogółkowo
Gogółkowo – wieś w Polsce , położona w województwie kujawsko-pomorskim , w powiecie żnińskim , w gminie Gąsawa .
Wieś Gokółkowo należąca do klucza żnińskiego arcybiskupstwa gnieźnieńskiego , położona była w XVII wieku w powiecie kcyńskim województwa kaliskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 398 mieszkańców .
Jest trzecią co do wielkości miejscowością gminy Gąsawa .
