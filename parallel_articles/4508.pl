Goiânia
Goiânia – miasto w środkowej Brazylii liczące razem z przedmieściami 2,2 mln mieszkańców , założone w latach 30 .
XX w. jako stolica stanu .
Ekonomia .
Ośrodek przemysłu spożywczego , włókienniczego , chemicznego i drzewnego oraz wydobycia rud miedzi , niklu , złota i diamentów .
Historia .
Miasto zostało utworzone w 1933 roku .
W 1937 roku rozpoczęto przenoszenie do Goiânii rządu stanowego , który w 1942 roku oficjalnie rozpoczął prace w nowej stolicy .
W 1987 roku w mieście doszło do skażenia promieniotwórczym izotopem cezu 137Cs .
W wyniku napromieniowania siedem osób zmarło .
100 000 osób zostało narażone na kontakt z radioaktywną substancją , z czego 1000 otrzymało dawki większe niż roczna dawka promieniowania naturalnego .
