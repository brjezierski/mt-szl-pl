Goldsboro ( Karolina Północna )
Goldsboro – miasto ( `` city '' ) , ośrodek administracyjny hrabstwa Wayne , we wschodniej części stanu Karolina Północna , w Stanach Zjednoczonych , położone w pobliżu rzeki Neuse .
W 2012 roku miasto liczyło 37 051 mieszkańców .
Miasto założone zostało w 1847 roku i nazwane zostało na cześć Matthew T. Goldsborough , inżyniera linii kolejowej Wilmington and Weldon Railroad .
Goldsboro jest ośrodkiem handlu tytoniem a także ośrodkiem przemysłu lekkiego ( głównie odzieżowego , meblarskiego i spożywczego ) .
W mieście znajduje się Wayne Community College oraz baza US Air Force Seymour Johnson Air Force Base .
23 stycznia 1961 roku nieopodal Goldsboro w następstwie awarii bombowca B-52 Startofortress zrzucone zostały dwie bomby jądrowe Mark 39 , z których jedna rozpoczęła procedurę detonacji .
Incydent ten był bliski spowodowania katastrofy nuklearnej .
