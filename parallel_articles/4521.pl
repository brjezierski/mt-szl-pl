Golovinomyces cichoracearum
Golovinomyces cichoracearum ( DC . )
V.P .
Heluta – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Występuje głównie na licznych gatunkach roślin z rodziny astrowatych .
Pasożyt obligatoryjny wywołujący chorobę o nazwie mączniak prawdziwy astrowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Golovinomyces , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1805 roku Augustin Pyramus de Candolle nadając mu nazwę `` Erysiphe cichoracearum '' Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1988 roku V.P .
Heluta , przenosząc go do rodzaju `` Golovinomyces '' .
Niektóre synonimy :
Lektotyp : na `` Scorzonera hispanica '' we Francji .
Morfologia .
Grzyb mikroskopijny .
Cienka lub średniogruba grzybnia rozwija się głównie na powierzchni liści i łodyg , do komórek skórki zapuszcza tylko ssawki .
