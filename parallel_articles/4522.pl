Golovinomyces orontii
Golovinomyces orontii ( Castagne ) V.P .
Heluta – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Wraz z `` Podosphaera fusca '' wywołuje u roślin chorobę o nazwie mączniak prawdziwy dyniowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Colovinomyces , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1851 roku Jean Louis Martin Castagne nadając mu nazwę `` Erysiphe orontii . ''
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1988 roku V.P .
Heluta , przenosząc go do rodzaju `` Golovinomyces '' .
