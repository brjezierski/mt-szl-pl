Morfologia .
Grzyb mikroskopijny .
Grzybnia wygląda jak biały , mączysty nalot , jest rozproszona lub tworzy skupiska .
Strzępki białe , rozgałęzione , lekko powyginane , o szerokości 5–7 μm .
Konidiofory proste , lub nieco zakrzywione , cylindryczne , do kolanek mają długość 40–100 × 10–13 μm , a dalej posiadają 1-3 krótsze komórki ( o długości 10–25 × 11–14 μm ) .
