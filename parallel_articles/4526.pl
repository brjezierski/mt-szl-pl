Goleniów ( gmina w województwie wrocławskim )
Goleniów ( alt .
Golanów lub Galonów ) – dawna gmina wiejska istniejąca w latach 1945–1946 w woj .
wrocławskim ( dzisiejsze woj .
dolnośląskie ) .
Siedzibą władz gminy był Goleniów ( Golanów ) , obecnie część Kamieńca Ząbkowickiego o nazwie `` Goleniów Śląski . ''
Gmina `` Golanów '' powstała po II wojnie światowej na terenie tzw .
Ziem Odzyskanych ( tzw .
II okręg administracyjny – `` Dolny Śląsk '' ) , obok krótkotrwałego miasta Kamieniec Ząbkowicki ( 1945–46 ) .
28 czerwca 1946 gmina – jako jednostka administracyjna powiatu ząbkowickiego – weszła w skład nowo utworzonego woj .
wrocławskiego .
Według stanu z 28 czerwca 1946 gmina liczyła 6216 mieszkańców .
Gminę zniesiono krótko po tym , włączając ją do nowo utworzonej gminy Kamieniec Ząbkowicki , powstałej po zniesieniu miasta Kamieniec .
