Goloki
Goloki ( Golocy , Goloucy , Golouki ) – mała grupa etnograficzna ludności polskiej mieszkająca na Górnym Śląsku , za Odrą , w części powiatu prudnickiego , na wschód od Prudnika .
Podgrupa Opolan .
Posługują się gwarą prudnicką , którą charakteryzuje brak mazurzenia , dyftongizacja samogłosek , wymowa `` ã '' we wszystkich pozycjach i pomieszanie we fleksji tematów twardych i miękkich , czyli też i po twardych jako końcówka : stari „ stary ” , żywi „ żywy ” .
Nazwa „ Goloki ” pochodzi od nazwy miasta Głogówek .
