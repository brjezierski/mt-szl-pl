Goma
Goma – miasto w Demokratycznej Republice Konga , stolica prowincji Kiwu Północne .
Miasto położone jest nad jeziorem Kiwu oraz w sąsiedztwie wulkanu Nyiragongo oraz Parku Narodowego Wirunga .
Goma posiada małe lotnisko oraz swój początek ma tutaj droga krajowa nr 2 do Kisangani .
W Bumi prowadzi misję Zjednoczony Kościół Metodystyczny , który liczy 8445 członków w 48 kościołach .
