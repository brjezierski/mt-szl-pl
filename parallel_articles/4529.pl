Gonaïves
Gonaïves ( po haitańsku : `` Gonayiv '' ) – miasto w północnej części Haiti , stolica departamentu Artibonite ; ok. 154 tys .
mieszkańców ( szacunkowo za 2012 r. ) – czwarte co do wielkości miasto kraju .
Gonaïves nazywane jest czasami `` miastem niepodległości '' , ponieważ to właśnie tutaj , 1 stycznia 1804 r. Jean-Jacques Dessalines proklamował niepodległość kraju .
W 1994 r. podczas manifestacji , wyrażających poparcie dla Jeana-Bertranda Aristide , armia dokonała w mieście masakry protestujących .
Dziesięć lat później Gonaïves było sceną rozruchów , tym razem przeciwko Aristide'owi .
