Google Chrome
Google Chrome , wymowa : [ ] – darmowa przeglądarka internetowa rozwijana przez Google .
Jej kod został napisany w oparciu o rozwiązania open source częściowo oparte na innych aplikacjach ( m.in .
WebKit i Mozilla ) , z wyjątkiem wersji na iOS .
Od wydania wersji 28 przeglądarka wykorzystuje silnik Blink .
Przeglądarkę tę charakteryzuje oszczędność miejsca , jakie zajmuje interfejs .
Pierwsza wersja beta została wydana 2 września 2008 , a pierwsza stabilna wersja została udostępniona 11 grudnia 2008 .
Według serwisu Statcounter.com w styczniu 2019 roku był najczęściej używaną przeglądarką internetową na świecie i używało jej 61,72 % internautów .
Według serwisu Netmarketshare.com w styczniu 2019 używana była przez około 67,29 % internautów .
