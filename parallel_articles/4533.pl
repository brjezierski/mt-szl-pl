Goran Bregović
Goran Bregović ( w zapisie serbską cyrylicą : Горан Бреговић ; ur .
22 marca 1950 w Sarajewie ) – bośniacki kompozytor muzyki rockowej , elektronicznej i etnicznej .
W swojej twórczości łączy m.in .
tradycyjne brzmienia ludowe z męskimi chórami i dźwiękami z syntezatora , chociaż zasłynął jako lider i autor muzyki zespołu rockowego Bijelo dugme .
Życiorys .
Dzieciństwo .
Jest synem Serbki i Chorwata .
Wychował się w Sarajewie , stolicy i centrum kulturalnym Bośni .
Ma brata Predraga , który mieszka w Nowym Jorku , i siostrę Dajanę , która mieszka w Splicie .
Kariera .
W latach 70. i 80 .
XX wieku był liderem popularnego jugosłowiańskiego zespołu rockowego Bijelo dugme .
Współpracował m.in .
z reżyserem Emirem Kusturicą , tworząc ścieżkę dźwiękową do filmów `` Arizona Dream '' , `` Czas Cyganów '' , czy `` Underground '' .
Współpracował także z Patrice ’ em Chereau , pisząc muzykę do filmu `` Królowa Margot '' .
