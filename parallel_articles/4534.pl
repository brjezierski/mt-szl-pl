Gorawino
Gorawino ( niem .
`` Gervin '' ) – wieś w Polsce położona w województwie zachodniopomorskim , w powiecie kołobrzeskim , w gminie Rymań .
Wieś jest siedzibą sołectwa `` Gorawino '' , w skład którego wchodzi również osada Rębice .
Według danych z 4 września 2013 r. Gorawino miało 671 mieszkańców .
