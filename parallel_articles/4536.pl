Goring-on-Thames
Goring-on-Thames lub Goring – wieś i civil parish w Anglii , w hrabstwie Oxfordshire , w dystrykcie South Oxfordshire .
Leży 27 km na południe od Oksfordu i 70 km na zachód od Londynu .
W 2011 roku civil parish liczyła 3187 mieszkańców .
Goring jest wspomniana w Domesday Book ( 1086 ) jako `` Garinges '' .
W tej miejscowości mieszkał i zmarł brytyjski piosenkarz George Michael .
