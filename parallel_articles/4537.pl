Gorycja
Gorycja ( , , , ) – miasto i gmina we Włoszech , w regionie Friuli-Wenecja Julijska , w prowincji Gorycja , przy granicy ze Słowenią , u podnóża Alp Julijskich .
Siostrzane miasto słoweńskiej Novej Goricy .
Nazwa miasta pochodzi prawdopodobnie od słoweńskiego słowa `` gorica '' , oznaczającego `` niewielkie wzgórze '' .
W latach 40 .
XVI w. funkcję medyka miejskiego w Gorycji pełnił znany botanik i naturalista Pierandrea Matthioli , który kontynuował tu swoje obserwacje botaniczne i przekładał z greki „ Materia Medica ” Dioskurydesa .
Historia .
Pierwsza pisemna wzmianka o Gorycji pochodzi z dnia 28 kwietnia 1001 , z dokumentu cesarza Ottona III , który darował zamek i osadę `` Goriza '' patriarsze Akwilei Janowi II i hrabiemu Friuli .
W dokumencie tym Gorycja była opisana jako `` wieś zwana w języku słowiańskim Goriza `` ( „ Villa quae Sclavorum lingua vocatur Goriza ” ) .
