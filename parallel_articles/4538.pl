Czekolada pitna
Czekolada pitna , czekolada do picia – napój powstały z mieszanki śmietanki ( 30-36 % ) , mleka i czekolady gorzkiej ( wersja klasyczna ) , zazwyczaj wypijany na gorąco .
Do Europy została przywieziona z Nowego Świata .
W XIX- i XX-wiecznych europejskich lokalach gastronomicznych podawano czekoladę pitną przyrządzaną na gorąco i na zimno , z dodatkiem spienionego mleka lub bitej śmietany , na ostro i w stylu meksykańskim oraz w połączeniu z kawą .
Serwowano również napój będący mieszanką białej i ciemnej czekolady .
Dodatkiem do czekolady bywa piana z mleka , które się spienia gorącą parą pod ciśnieniem , najczęściej w ekspresach ciśnieniowych .
