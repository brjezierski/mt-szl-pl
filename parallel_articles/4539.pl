Gorlice
Gorlice ( ) – miasto w województwie małopolskim , siedziba gminy wiejskiej Gorlice i powiatu gorlickiego .
Gorlice leżą w dolinie Ropy i jej dopływu Sękówki , na północnej granicy Beskidu Niskiego .
Przyległą zachodnią część Beskidu Niskiego powszechnie nazywa się Beskidem Gorlickim .
Najwyższy szczyt Gorlic stanowi wzgórze nazywane Łysą Górą ( 441 m n.p.m . )
.
Według danych GUS z 31 grudnia 2019 r. miasto liczyło 25 944 mieszkańców .
Składa się z 12 osiedli , które są jednostkami pomocniczymi gminy .
Pod względem historycznym Gorlice położone są w Małopolsce , na wschodnim skraju dawnej ziemi krakowskiej .
