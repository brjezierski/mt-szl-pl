Gornoałtajsk
Gornoałtajsk ( ) – miasto w Rosji , stolica Republiki Ałtaju , usytuowane w Południowej Syberii , w odległości 3641 km na wschód od Moskwy .
Jest to jedyne miasto w Republice Ałtaju .
Leży w wąskiej Dolinie Majma , u podnóża gór Ałtaj .
Historia .
Prawosławni misjonarze przybywający do doliny Majma w 1830 r. natknęli się na małą osadę Ułała , liczącą w tamtym czasie 19 tubylczych mieszkańców i trzy rosyjskie rodziny .
Rok później Ułała została wybrana jako miejsce pierwszej prawosławnej misji w Ałtaju , dzięki czemu rosyjscy osadnicy zaczęli się tu osiedlać , tworząc pierwsze wsie .
W 1922 r. utworzono autonomiczny region Ojrot , a Ułała stała się jego stolicą .
W 1928 r. wieś otrzymała prawa miejskie .
W 1932 r. zmieniono nazwę na Ojrot-Tura .
W 1948 r. nazwa autonomii została zmieniona na Gorno-Ałtajski Region Autonomiczny , stąd też nazwa jego stolicy .
Gornoałtajsk ma rozproszony przemysł , posiada lotnisko , teatr , uniwersytet i regionalne muzeum .
Do najbliższego dworca kolejowego w Bijsku jest 96 km .
