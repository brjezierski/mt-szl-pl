Gorzany
Gorzany – wieś w Polsce , położona w województwie kujawsko-pomorskim , w powiecie inowrocławskim , w gminie Pakość .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 166 mieszkańców .
Jest dziesiątą co do wielkości miejscowością gminy Pakość .
