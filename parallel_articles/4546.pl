Gorzelin ( przystanek kolejowy )
Gorzelin – przystanek kolejowy w województwie dolnośląskim w Polsce .
Przystanek posiada jeden peron z krawędzią oraz murowaną wiatę stacyjną wybudowaną przez mieszkańców w czasach PRL jako czyn społeczny .
Od 1 września 2010 nie zatrzymuje się już to żaden pociąg pasażerski .
W 2019 roku została wyremontowana linia kolejowa nr 289 i przywrócono na niej ruch pasażerski .
W okresie od czerwca 2019 do maja 2020 ze stacji Gorzelin skorzystało 1896 podróżnych .
Od 12 grudnia 2021 przystanek na żądanie .
