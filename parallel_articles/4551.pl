Gostchorz
Gostchorz ( daw .
Goschorz ) – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Leży nad Muchawką .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Jezusowego w Wiśniewie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Przez wieś przebiega droga krajowa nr 63 Węgorzewo – Sławatycze i dwie drogi powiatowe : do Domanic i do Kaczor .
Znajduje się tu Szkoła Podstawowa im .
Kazimierza Odnowiciela oraz stary młyn .
W miejscowości działa założona w 1923 roku jednostka ochotniczej straży pożarnej .
Na wyposażeniu jednostki znajdują się dwa samochody gaśnicze : średni GBA 2,8/16 MAN z 1994 roku oraz lekki GLBM Fiat Ducato z 1997 roku .
