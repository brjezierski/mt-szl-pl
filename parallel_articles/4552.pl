Gostchorze
Gostchorze ( niem .
`` Goskar '' ) – wieś w Polsce położona w województwie lubuskim , w powiecie krośnieńskim , w gminie Krosno Odrzańskie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zielonogórskiego .
Sołtys Ewa Klepczyńska otrzymała tytuł `` Niezwykła Polka '' w akcji Akacja 2007 , oraz `` Wicelubuszanin 2007 roku '' , w plebiscycie Gazety Lubuskiej .
Grodzisko Gostchorze .
W dobie piastowskiej Gostchorze było ważnym punkiem na strategicznej mapie ziemi krośnieńskiej .
Ponad 40 lat temu archeolog Edward Dąbrowski , dokonał obserwacji terenowych , które pozwoliły rozpoznać system obronny wczesnopiastowskiego państwa na polsko-niemieckim pograniczu .
Na wysokim cyplu , po drugiej stronie Odry , oddalonym o 4,7 km od krośnieńskiej warowni wznosił się kolejny gród .
