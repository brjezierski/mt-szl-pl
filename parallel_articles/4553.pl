Gostyń ( stacja kolejowa )
Gostyń ( stacja kolejowa ) – dworzec kolejowy w Gostyniu w woj .
wielkopolskim , w powiecie gostyńskim .
Znajduje się przy ulicy Dworcowej .
Budynek gostyńskiego dworca ( nieco podobny do dworca na stacji kolejowej w Jarocinie ) jest budynkiem piętrowym , zbudowanym z czerwonej cegły licowej z wielopłaszczyznowym dachem i stosunkowo dużym w porównaniu z wielkością miasta .
O jego budowie zadecydowało połączenie kolejowe Leszna z Jarocinem , które nastąpiło 1 października 1888 roku .
W grudniu 2011 roku na odcinku z Kąkolewa do Leszna został zawieszony ruch pasażerski .
Od 2 marca 2012 r. Koleje Wielkopolskie zawiesiły na stałe kursowanie pociągów na trasie Gostyń - Leszno .
