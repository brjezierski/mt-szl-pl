Gostynin ( stacja kolejowa )
Gostynin – stacja kolejowa na linii kolejowej nr 33 ( Kutno – Brodnica ) , położona w województwie mazowieckim .
Stacja ma 2 perony .
Peron 1 jest jednokrawędziowy , natomiast peron 2 jest dłuższy i posiada dwie krawędzie .
Oprócz tego stacja ma tory przeznaczone dla pociągów towarowych czy tory ładunkowe lub bocznicę kolejową .
W 2018 roku stacja obsługiwała 50–99 pasażerów na dobę .
Ze stacji kolejowej w Gostyninie odjeżdżają pociągi do Sierpca , Płocka , Kutna oraz Warszawy Wschodniej .
Budynek dworcowy został wybudowany w 1924 r. , według typowego projektu stacji średniej wielkości zastosowanego również m.in .
