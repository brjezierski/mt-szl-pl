Goszowice
Goszowice ( niem .
`` Kuschdorf '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie nyskim , w gminie Pakosławice .
Do sołectwa Goszowice przynależą : Frączków , Śmiłowice i Naczków .
W latach 1975–1998 wieś położona była w ówczesnym województwie opolskim .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Coschovitz '' oraz `` Godcowitz '' w szeregu wsi lokowanych na prawie polskim `` iure polonico '' .
