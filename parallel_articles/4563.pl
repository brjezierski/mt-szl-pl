Głowa
Głowa ( ) – część ciała zwierząt , zajmująca u człowieka i u innych ssaków szczytowe umiejscowienie ( ewentualnie przednie ) .
Szyja ( `` collum '' ) stanowi podporę dla głowy oraz drogę łączącą ją z tułowiem i kończyną górną .
Kostne rusztowanie głowy stanowi czaszka ( `` cranium '' ) , zapewniająca ochronę zawartego w niej mózgu ( `` cerebrum '' ) .
W obrębie przedniej części czaszki , tzw .
trzewioczaszki , osadzony jest narząd wzroku oraz początek dróg : pokarmowej i oddechowej .
W części skroniowej czaszki znajduje się narząd słuchu .
Głowa dorosłego człowieka waży około 4–5 kg .
U niektórych stawonogów ( skorupiaki i szczękoczułkowce ) wykształcił się głowotułów .
U skorupiaków jest to część ciała powstała ze zrośnięcia się głowy z częścią segmentów tułowia ( 1-5 ) , gdzie odcinek głowowy nie jest podzielony na segmenty .
U szczękoczułkowców głowotułów powstał w rozwoju osobniczym .
Głowa zrosła się ze wszystkimi segmentami tułowia .
U większości grup brak podziału na segmenty ( szczątkowa segmentacja występuje u solfug ) .
Od wyrazu `` głowa '' w wielu językach pochodzą pojęcia określające rzeczy uznane za najważniejsze dla czegoś , jak `` głowa państwa '' czy wywodzące się z łac .
