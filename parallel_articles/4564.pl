Gowidlino
Gowidlino ( kaszb .
Gòwidlëno ) – wieś letniskowa w Polsce , położona w województwie pomorskim , w powiecie kartuskim , w gminie Sierakowice .
Duża kaszubska wieś leży na obszarze moreny dennej Pojezierza Kaszubskiego , nad północnym brzegiem jeziora Gowidlińskiego przy drodze wojewódzkiej nr 211 ( Żukowo-Kartuzy-Sierakowice-Nowa Dąbrowa ) .
Miejscowość jest siedzibą sołectwa Gowidlino , w którego skład wchodzą również Lemany , Kawle , Dolina Jadwigi , Gowidlinko i Gowidlino-Wybudowanie .
W Gowidlinie znajdują się liczne kwatery agroturystyczne i plantacje truskawek .
Wieś królewska w starostwie mirachowskim w województwie pomorskim w II połowie XVI wieku .
W latach 1954–1971 wieś należała i była siedzibą władz gromady Gowidlino , po jej zniesieniu w gromadzie Sierakowice .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Historia .
Pierwsze wzmianki o miejscowości pochodzą z XIV wieku .
Administracyjnie wieś podlegała pod ówczesny powiat mirachowski .
Od 1920 r. wieś znajdowała się ponownie w granicach Polski ( powiat kartuski ) .
Historycznie ukształtowana niemiecka nazwa wsi była identyczna z polską .
Ze względu na jej słowiańskie brzmienie , w 1942 r. administracja hitlerowska przemianowała miejscowość na `` Göbeln '' .
