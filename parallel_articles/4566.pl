Gaz
Gaz – stan skupienia materii , w którym ciało fizyczne łatwo zmienia kształt i zajmuje całą dostępną mu przestrzeń .
Właściwości te wynikają z własności cząsteczek , które w fazie gazowej mają pełną swobodę ruchu .
Wszystkie one cały czas przemieszczają się w przestrzeni zajmowanej przez gaz i nigdy nie zatrzymują się w jednym miejscu .
Między cząsteczkami nie występują żadne oddziaływania dalekozasięgowe , a jeśli , to bardzo słabe .
Jedyny sposób , w jaki cząsteczki na siebie oddziałują , to zderzenia .
Oprócz tego , jeśli gaz jest zamknięty w naczyniu , to jego cząsteczki stale zderzają się ze ściankami tego naczynia , wywierając na nie określone i stałe ciśnienie .
Termin wprowadzony przez flamandzkiego lekarza Johanna Helmonta w XVII wieku wzorem gr .
`` χάος '' ‘ cháos ’ .
