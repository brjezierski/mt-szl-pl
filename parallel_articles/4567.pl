Gołasze-Puszcza
Gołasze-Puszcza – wieś sołecka w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Bartłomieja Apostoła w Kuleszach Kościelnych do parafii św .
Jana Chrzciciela w Wysokiem Mazowieckiem .
