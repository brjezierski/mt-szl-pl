Gołczewo
Gołczewo ( dodatkowa nazwa w j. kaszub .
Gôłczewò ) – wieś kaszubska w Polsce na pograniczu pojezierzy Kaszubskiego i Bytowskiego położona w województwie pomorskim , w powiecie bytowskim , w gminie Parchowo .
Miejscowość jest również placówką Ochotniczej Straży Pożarnej .
Na południu znajduje się jezioro Stropno .
Wieś królewska położona była w II połowie XVI wieku w powiecie mirachowskim województwa pomorskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
Historia .
Do 1918 wieś znajdowała pod administracją zaboru pruskiego .
