Przerośl Gołdapska
Przerośl Gołdapska – osada w Polsce położona w województwie warmińsko-mazurskim , w powiecie gołdapskim , w gminie Dubeninki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
