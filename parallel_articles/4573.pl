Gołoszyce
Gołoszyce – wieś sołecka w Polsce , położona w województwie świętokrzyskim , w powiecie opatowskim , w gminie Baćkowice .
Po II wojnie siedziba gminy Modliborzyce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnobrzeskiego .
Części wsi .
Gołoszyce są punktem początkowym Głównego Szlaku Świętokrzyskiego im .
Edmunda Massalskiego , prowadzącego do Kuźniaków , czerwonego szlaku turystycznego prowadzącego do Piotrowic oraz niebieskiego szlaku turystycznego , prowadzącego do Dwikoz .
