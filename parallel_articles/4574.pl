Gołębowo
Gołębowo – wieś sołecka w Polsce położona w województwie wielkopolskim , w powiecie obornickim , w gminie Oborniki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
Zabytki i historia .
We wsi znajdują się pozostałości zespołu dworskiego : dwór murowany Chorzemskich z początków XIX wieku ( przebudowany w 1921 , renowatorem był Andrzej Degórski ) , park krajobrazowy z 1. połowy XIX wieku , obora , stodoła , budynek mieszkalny , kuźnia i brama .
Sam dwór ozdobiony ryzalitem w wielkim porządku , monogramem `` JCH '' i datą `` 1921 '' .
Od strony ogrodowej napis na portyku , zgodny z wielkopolskim etosem pracy organicznej : `` Wiara i praca to nasza moc '' .
Początkowo Gołębowo stanowiło część majątku w Ocieszynie , a potem się uniezależniło .
W 1926 dobra liczyły 341 ha .
