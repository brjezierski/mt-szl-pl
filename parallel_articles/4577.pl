Gościce
Gościce ( niem .
`` Gostitz '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Paczków .
Gościce leżą na Przedgórzu Paczkowskim , liczba mieszkańców to około 460 osób .
Przedłużeniem wsi w Czechach jest wieś Horní Hoštice .
W latach 1945–1991 stacjonowała tu strażnica Wojsk Ochrony Pogranicza , która podlegała 45 Batalionowi WOP w Prudniku .
Z dniem 16 maja 1991 roku strażnica została przejęta przez Straż Graniczną i funkcjonowała do 1 stycznia 2003 roku , kiedy to została rozformowania .
