Goświnowice ( stacja kolejowa )
Goświnowice – stacja kolejowa w miejscowości Goświnowice , w województwie opolskim , w powiecie nyskim , w gminie Nysa , w Polsce .
Jest to ważna stacja ruchu towarowo-przeładunkowego , mniejsze znaczenie ma dla ruchu pasażerskiego ( w okresie 2010 - 2018 brak takiego ruchu , od 2018 wznowiono jako ruch sezonowy ) .
Dawna , niemiecka nazwa stacji brzmiała `` Giesmannsdorf '' .
Historia .
