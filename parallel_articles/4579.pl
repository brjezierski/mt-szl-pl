Według stanu z 1 lipca 1952 roku gmina była podzielona na 5 gromad : Biała Góra , Gościszewo , Gronajny , Koniecwałd i Uśnice .
Gmina została zniesiona 29 września 1954 roku wraz z reformą wprowadzającą gromady w miejsce gmin .
Jednostki nie przywrócono 1 stycznia 1973 roku wraz z kolejną reformą reaktywującą gminy .
