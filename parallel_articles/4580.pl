Graaff-Reinet
Graaff-Reinet to liczące 33.000 mieszkańców miasto , położone w Prowincji Przylądkowej Wschodniej w Republice Południowej Afryki .
Geografia .
Miasto leży w krainie geograficznej Karru u podnóży `` Sneuberg-Mountains '' i otoczone jest z trzech stron wijącą się wstęgą rzeki Sundays River .
Graaff-Reinet posiada prawie 200 zabytków unikalnej holenderskiej architektury , które przyciągają tu dużą liczbę turystów .
Miasto jest ważnym ośrodkiem gospodarczym w regionie .
Gospodarka opiera się na hodowli ( przede wszystkim owiec i strusiów ) .
Poza tym miasto jest ważnym ośrodkiem uprawy winorośli .
Historia .
Miasto założone zostało 19 lipca 1786 przez Holendrów , pod patronatem Holenderskiej Kompanii Wschodnioindyjskiej .
Było to czwarte miasto w Południowej Afryce po Kapsztadzie , Stellenbosch i Swellendam .
Nazwa została nadana na cześć Holendra Corneliusa Jacoba van Graafa i jego żony gubernator Cornelii Reinet .
W 1792 roku powstała pierwsza świątynia Holenderskiego Kościoła Reformowanego .
6 lutego 1795 Burscy koloniści w Graaf Reinet zbuntowali się przeciw rządom Holenderskiej Kompanii Wschodnioindyjskiej i założyli pierwszą samorządną Republikę burską - Republiek Graaff Reinet .
Zlikwidowana została ona 22 sierpnia 1796 .
