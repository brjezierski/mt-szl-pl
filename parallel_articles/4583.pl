Grabianów
Grabianów – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 5 km na południe od Siedlec .
Wieś szlachecka położona była w drugiej połowie XVI wieku w ziemi łukowskiej województwa lubelskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W odległości 1 km od Grabianowa przebiega droga krajowa nr 63 Węgorzewo - Sławatycze .
Bezpośrednio na południe znajduje się obwodnica Siedlec , leżąca w ciągu drogi krajowej nr 2 .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Jana Pawła II w Siedlcach .
W Grabianowie urodził się Lucjan Koć żołnierz Batalionów Chłopskich .
Historia .
Pierwsza pisemna wzmianka o miejscowości pochodzi z 1471 - Grabianów jest wymieniony jako jedna z wsi wchodzących w skład nowo erygowanej parafii w Pruszynie .
W pierwszej połowie XVI w. Grabianów znalazł się w posiadaniu właścicieli Siedlec - Gniewoszów-Siedleckich .
Od 1532 wszedł w skład nowo utworzonej parafii siedleckiej .
W następnych stuleciach Grabianów stanowił prywatną własność kolejnych właścicieli Siedlec .
W 1827 liczył 18 domów i 135 mieszkańców .
W 1881 miał 25 domów i 225 mieszkańców .
