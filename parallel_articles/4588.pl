Grabs
Grabs – szwajcarska gmina w kantonie Sankt Gallen w dystrykcie Werdenberg .
Grabs graniczy z gminami : Alt Sankt Johann , Buchs , Gams , Sevelen , Walenstadt , Wildhaus .
Liczba ludności zgodnie z danymi ze stycznia 2004 roku wynosi 6 352 .
Powierzchnia gminy to 54.64 km² .
Średnia wysokość bezwzględna równa jest 465 metrów .
Kod pocztowy do Grabs to 9472 .
