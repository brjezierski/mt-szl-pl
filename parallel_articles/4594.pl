Grainau
Grainau – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , w powiecie Garmisch-Partenkirchen .
Leży w górach w Wettersteingebirge , około 5 km na zachód od Garmisch-Partenkirchen , nad Loisach , przy drodze B23 i linii kolejowej Kempten ( Allgäu ) - Reutte ( Austria ) - Garmisch-Partenkirchen , u podnóża Zugspitze .
Polityka .
Wójtem gminy jest Andreas Hildebrandt z CSU , rada gminy składa się z 16 osób .
Oświata .
W gminie znajduje się przedszkole ( 75 miejsc ) oraz szkoła podstawowa ( 7 nauczycieli , 118 uczniów ) .
