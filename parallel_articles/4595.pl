Grainet
Grainet – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Bawaria , w regionie Donau-Wald , w powiecie Freyung-Grafenau .
Leży w Lesie Bawarskim , około 8 km na wschód od miasta Freyung .
Dzielnice .
W skład gminy wchodzą następujące dzielnice : Böhmzwiesel , Fürholz , Grainet , Rehberg , Vorderfreundorf .
Polityka .
Wójtem jest Kaspar Vogl z SPD , rada gminy składa się z 14 osób .
Oświata .
W gminie znajduje się 75 miejsc przedszkolnych oraz szkoła podstawowa .
