Grand Forks ( Dakota Północna )
Grand Forks - miasto w Stanach Zjednoczonych , w stanie Dakota Północna , nad rzeką Red River .
W okolicy znajduje się główna baza strategicznych sił jądrowych amerykańskich sił powietrznych , w której stacjonują wielogłowicowe pociski balistyczne klasy ICBM Minuteman III .
