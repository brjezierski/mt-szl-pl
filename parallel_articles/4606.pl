Grandville
Grandville , właśc .
Jean Ignace Isidore Gérard ( ur .
13 września 1803 w Nancy , zm .
17 marca 1847 w Vanves ) – francuski grafik , karykaturzysta i ilustrator oraz artysta fantastyczny .
Pod pseudonimem Grandville drukował w pismach i publikacjach karykatury , zwłaszcza polityczne .
Porównywany z Honoré Daumierem i Gustave'em Dorém , posługiwał się często popularną i wydajną techniką stalorytu .
W 1844 wydał książkę z ilustracjami pt .
`` Un autre monde '' .
