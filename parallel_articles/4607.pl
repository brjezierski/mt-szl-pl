Grangemouth
Grangemouth ( gael .
`` Inbhir Ghrainnse '' ) – miasto w Szkocji , w jednostce administracyjnej Falkirk , historycznie w hrabstwie Stirlingshire .
Port morski położony na południowym brzegu zatoki Firth of Forth ( ok. 40 km na zachód od centrum Edynburga i ok. 5 km na wschód od Falkirk ) u ujścia potoku Grange Burn oraz rzeki Carron do estuarium utworzonego przez ujście rzeki Forth do Morza Północnego .
W 1768 roku w związku z budowanym kanałem Forth and Clyde ( oddanym do eksploatacji w 1790 ) powstała w miejscu obecnego miasta osada , nazwana `` Sealock '' , później przemianowano ją na `` Grangeburnmouth '' i ostatecznie na `` Grangemouth '' .
Według spisu ludności z 2001 roku miasto liczyło 17 906 mieszkańców .
W porcie Grangemouth znajduje się największy terminal kontenerowy Szkocji przeładowujący rocznie 9 milionów ton ładunków .
W mieście zlokalizowana jest także rafineria ropy naftowej o wydajności 210 tys .
