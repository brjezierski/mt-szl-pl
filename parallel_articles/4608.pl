Granges-sur-Vologne
Granges-sur-Vologne – miejscowość i dawna gmina we Francji , w regionie Grand Est , w departamencie Wogezy .
W 2013 roku populacja ludności wynosiła 2294 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Aumontzey oraz Granges-sur-Vologne – utworzono nową gminę Granges-Aumontzey .
Siedzibą gminy została miejscowość Granges-sur-Vologne .
