Od XVIII wieku Grasse słynie na cały świat ze swego przemysłu perfumeryjnego .
Jest francuskim i światowym centrum produkcji naturalnych aromatów ( `` la capitale mondiale des parfums '' ) .
Dochód z tej produkcji wynosi ponad 600 mln € na rok .
W Grasse działają po dziś dzień najsłynniejsze i najstarsze wytwórnie perfum : utworzona w 1747 roku `` Galimard Parfumeur '' , powstała w 1849 `` Molinard Parfumeur '' oraz `` Fragonard Parfumeur '' .
