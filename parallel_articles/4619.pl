Graz
Graz ( słoweń .
`` Gradec '' ) – miasto statutarne w południowo-wschodniej Austrii , stolica kraju związkowego Styria oraz siedziba powiatu Graz-Umgebung ( do którego jednak nie należy ) .
Miasto leży nad rzeką Murą , u podnóża Alp Styryjskich .
Po Wiedniu drugie co do wielkości i znaczenia miasto Austrii .
Graz jest znaczącym ośrodkiem przemysłowym , kulturalnym i turystycznym .
W mieście znajdują się cztery uniwersytety i dwie inne szkoły wyższe , do których uczęszcza razem ponad 45 tys .
studentów .
W roku 2003 był kulturalną stolicą Europy .
Znajduje się tu także polski konsulat honorowy .
Toponimia nazwy .
Nazwa miasta wywodzi się od słowa `` gradec '' – słoweńskiego wyrazu oznaczającego mały zamek .
Jest to związane z tym , że miasto Graz powstało na terenie wcześniejszej słowiańskiej osady o takiej właśnie nazwie .
Historia .
Pierwsze wzmianki o Grazu pochodzą z roku 881 , kiedy w dokumencie wymienia się go jako warownię władców Styrii .
Potwierdzeniem statusu było przyznanie praw
miejskich w roku 1281 przez Rudolfa I Habsburga .
Od pierwszej połowy XV wieku aż do XVII wieku Graz był siedzibą styryjskiej gałęzi dynastii Habsburgów .
