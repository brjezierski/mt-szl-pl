Smętowo Graniczne
Smętowo Graniczne – wieś kociewska w Polsce położona w województwie pomorskim , w powiecie starogardzkim , w gminie Smętowo Graniczne .
Miejscowość leży na ważnej kolejowej magistrali węglowej `` Śląsk – Porty '' .
Do momentu wstrzymania ruchu kolejowego na liniach do Opalenia i Skórcza stacja w Smętowie była ważnym węzłem kolejowym .
W latach 1954-1972 wieś należała i była siedzibą władz gromady Smętowo Graniczne .
W latach 1945–1975 miejscowość administracyjnie należała do tzw .
