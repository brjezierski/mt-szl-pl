Graniczna Woda
Graniczna Woda ( niem .
`` Grenzwasser '' , `` Grenzbach '' ) – potok , prawostronny dopływ Stoły o długości 11,7 km .
Płynie przez powiat tarnogórski , przez gminy Miasteczko Śląskie , Tarnowskie Góry i Tworóg .
Potok rozpoczyna swój bieg w Miasteczku Śląskim w okolicach huty cynku , następnie przepływa przez Tarnowskie Góry ( Jezioro Głęboki Dół ) i Boruszowice , gdzie wpada do Stoły .
Na krótkim odcinku , na terenie nieistniejącego już rezerwatu Dęby Boruszowickie , jest potokiem meandrującym .
