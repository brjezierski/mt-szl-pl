Grażyna Dziedzic ( pływaczka )
Grażyna Dziedzic ( ur .
14 stycznia 1963 w Oświęcimiu ) – trzynastokrotna mistrzyni Polski seniorów w pływaniu , brązowa medalistka mistrzostw Europy w Splicie , medalistka zawodów o puchar Europy w Göteborgu , trzykrotna finalistka mistrzostw świata w pływaniu w Ekwadorze .
Życiorys .
Naukę rozpoczęła w klasie sportowej w Szkole Podstawowej nr 8 w Oświęcimiu , ( późniejsza Szkoła Mistrzostwa Sportowego ) .
Nauczyła się pływać będąc w trzeciej klasie .
Rok później wystartowała w pierwszych zawodach jako kraulistka .
Wtedy przeszła pod opiekę trenera Kazimierza Woźnickiego , który odkrył jej predyspozycje do stylu klasycznego .
Pierwszy sukces odniosła w 1975 r. na mistrzostwach okręgu krakowskiego , zdobywając tytuł mistrzyni młodziczek na dystansie 800 m stylem dowolnym .
W tej samej kategorii zdobyła wicemistrzostwo Polski .
W 1977 r. , po otrzymaniu powołania do kadry narodowej juniorów , w ostatniej klasie szkoły podstawowej przeszła do nowo utworzonej Szkoły Mistrzostwa Sportowego w Raciborzu , gdzie miała rozwijać swój talent i możliwości .
Jednak tam trener Władimir Mitrofanow nie pokładał w Grażynie większych nadziei na sukces .
Wróciła więc do Oświęcimia i rozpoczęła naukę w Liceum im .
Stanisława Konarskiego w Oświęcimiu , które ukończyła w 1983 r. Równocześnie trenowała i odnosiła sukcesy jako podopieczna trenera Kazimierza Woźnickiego , w sekcji pływackiej KS Unia Oświęcim .
Karierę sportową zakończyła w 1984 r. W 1996 r. wygrała plebiscyt na najlepszego zawodnika 50-lecia Unii .
Obecnie pracuje w Miejskim Ośrodku Sportu i Rekreacji w Oświęcimiu .
