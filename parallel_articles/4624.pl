Grażyszki
Grażyszki ( lit .
`` Gražiškiai '' ) – miasteczko na Litwie w okręgu mariampolskim w rejonie Wyłkowyszki , położone ok. 24 km na południe od Wyłkowszek , siedziba starostwa Grażyszki , zlokalizowane przy drodze Kalwaria-Wisztyniec .
Znajduje się tu poczta , kościół parafialny i cmentarz .
Wieś założona w XVI wieku , w 1746 uzyskała przywilej na organizację tragów .
Na podstawie dekretu prezydenta Republiki Litewskiej z 2001 roku miasteczko otrzymało herb .
Za Królestwa Polskiego siedziba wiejskiej gminy Wojtkobole .
