Etymologia nazwy .
Określenia „ język grecki ” , „ greka ” pochodzą od Rzymian , którzy nazywali ten język po łacinie `` lingua Graeca '' .
Nazwa stosowana przez samych Greków w grece klasycznej brzmiała = `` he Hellenikè glõtta '' ( nowogr .
