Język grecki
Język grecki , greka ( , `` Hellenikè glõtta '' ; nowogr .
ελληνική γλώσσα , `` ellinikí glóssa '' lub ελληνικά , `` elliniká '' ) – język indoeuropejski z grupy helleńskiej , w starożytności ważny język basenu Morza Śródziemnego .
W kulturze europejskiej zaadaptowany , obok łaciny , jako język terminologii naukowej , wywarł wpływ na wszystkie współczesne języki europejskie , a także część pozaeuropejskich i starożytnych .
