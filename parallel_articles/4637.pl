Greenlawn
Greenlawn – jednostka osadnicza i miejscowość spisowa w hrabstwie Suffolk , w stanie Nowy Jork , w Stanach Zjednoczonych , założona w 1868 roku jako Old Fields .
Znajduje się na wyspie Long Island , w pobliżu miasta Huntington .
Ludność wynosiła ponad 13 tysięcy mieszkańców , według raportu z 2010 roku .
Studenci uczęszczają głównie do Harborfields High School .
