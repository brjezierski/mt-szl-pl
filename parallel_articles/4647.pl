Grenoble
Grenoble ( oksyt .
`` Grenòble '' , `` Granòble '' ; średniowieczny oksyt .
`` Grasanòbol '' ; franko-prow .
`` Grenoblo '' ) – miejscowość i gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Isère .
Przez miasto przepływa rzeka Isère .
Według danych na rok 2004 gminę zamieszkiwało 158 000 osób , a gęstość zaludnienia wynosiła 8315 osób/km² ( wśród 2880 gmin regionu Rodan-Alpy Grenoble plasuje się na 3. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 592 . ) .
W spisie z 2016 roku Grenoble miało 158 180 mieszkańców , podczas gdy populacja aglomeracji Grenoble ( po francusku : aire urbaine de Grenoble lub „ agglomération grenobloise ” ) wynosiła 687 985 , co czyni ją największą metropolią w Alpach , wyprzedzając Innsbruck i Bolzano .
Miasto jako znaczące europejskie centrum naukowe , reklamuje się jako „ stolica Alp ” , ze względu na swoją wielkość i bliskość gór .
