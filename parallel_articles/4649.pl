Grevenbroich
Grevenbroich [ ] – miasto powiatowe w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf , siedziba powiatu Rhein-Kreis Neuss .
W 2010 liczyło 63 891 mieszkańców .
Grevenbroich leży w trójkącie między miastami Düsseldorf , Kolonia , Mönchengladbach i znajduje się na 136. miejscu największych miast Niemiec .
Przez Grevenbroich przepływa rzeka Erft .
Wokół miasta odbywa się odkrywkowe wydobycie węgla brunatnego na potrzeby trzech elektrowni zlokalizowanych we Frimmersdorfie , Neurath i Niederaußem .
