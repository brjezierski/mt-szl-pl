Gryszkabuda ( gmina )
Gryszkabuda – dawna gmina wiejska istniejąca na przełomie XIX i XX wieku w guberni suwalskiej .
Siedzibą władz gminy była Gryszkabuda ( lit .
`` Griškabūdis '' ) , a następnie Blustrwiszki .
Za Królestwa Polskiego gmina Gryszkabuda należała do powiatu władysławowskiego w guberni suwalskiej ( od 1867 ) .
Po odzyskaniu niepodległości przez Polskę i Litwę powiat władysławowski na podstawie umowy suwalskiej wszedł 10 października 1920 w skład Litwy .
