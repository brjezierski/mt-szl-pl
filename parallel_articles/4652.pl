Grocholub
Grocholub ( dodatkowa nazwa w j. niem .
Grocholub ) – wieś w Polsce , położona w województwie opolskim , w powiecie krapkowickim , w gminie Walce .
Wieś zajmuje 7,04 km² , a mieszka w niej 7 % mieszkańców gminy .
Nazwa .
W swej historii wioska miała takie nazwy jak np .
Grocholuba , Grocholouw , Erbersdorf .
Historia .
Wieś wzmiankowana po raz pierwszy w łacińskim dokumencie z 1228 roku wydanym przez Kazimierza I opolskiego , gdzie zanotowana została w szeregu miejscowości założonych na prawie polskim `` iure polonico '' w zlatynizowanej , staropolskiej formie `` „ Groholuba ” '' .
