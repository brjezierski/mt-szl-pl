Grochowice
Grochowice – wieś w Polsce położona w województwie dolnośląskim , w powiecie głogowskim , w gminie Kotla .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa legnickiego .
Nazwa .
Nazwa miejscowości notowana była w formach `` Grochowicze '' ( ok. 1300 ) , `` Grochewicz '' ( 1453 ) , `` Grochewitcz '' ( 1488 ) , `` Gruchwitz '' ( 1670 ) , `` Grochwitz '' ( 1679 ) , `` Gruchwitz '' ( 1791 ) , `` Grochwitz , Alt- '' u .
`` Neu- '' , auch `` Gruchwitz '' ( 1845 ) , `` Heidegrund '' , `` Grochowiska '' ( 1941 ) , `` Grochowice '' – `` Heidegrund '' ( `` Grochwitz '' ) ( 1951 ) .
Nazwa wywodzi się od nazwy osobowej `` Groch '' z sufiksem `` -owice '' .
Po dojściu do władzy w Niemczech narodowych socjalistów nazwa wsi została zmieniona na całkowicie niemiecką `` Heidegrund '' .
