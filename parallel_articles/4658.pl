Grodków ( gmina )
Grodków – gmina miejsko-wiejska w województwie opolskim , w powiecie brzeskim .
W latach 1975–1998 gmina położona była w województwie opolskim .
Siedziba gminy to Grodków .
Według danych z 31 marca 2011 roku gminę zamieszkiwało 19847 osób .
Natomiast według danych z 31 grudnia 2017 roku gminę zamieszkiwało 19376 osób .
