Powiat grodziski ( województwo mazowieckie )
Powiat grodziski – powiat w Polsce ( województwo mazowieckie ) , utworzony w 1999 roku w ramach reformy administracyjnej .
Jego siedzibą jest miasto Grodzisk Mazowiecki .
Jego wschodnie obrzeża oddalone są zaledwie około 30 km na południowy zachód od Warszawy .
Jego powierzchnia wynosi 367 km2 , a liczba mieszkańców wynosi ogółem 88 634 ( dane GUS za 2014 rok ) .
Według danych z 31 grudnia 2019 roku powiat zamieszkiwały 95 963 osoby .
