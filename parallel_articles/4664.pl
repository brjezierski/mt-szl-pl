Groitzsch
Groitzsch ( pol .
hist .
`` Grójec '' ) – miasto w Niemczech , w kraju związkowym Saksonia , w okręgu administracyjnym Lipsk , w powiecie Lipsk ( do 31 lipca 2008 w powiecie Leipziger Land ) .
Geografia .
Groitzsch leży ok. 25 km na południe od Lipska i ok. 20 km na północny wschód od Zeitz .
