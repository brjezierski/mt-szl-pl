Historia .
Miasto zostało założone przez Rosjan w 1818 roku jako placówka wojskowa podczas wojny kaukaskiej .
Pierwotnie było zamieszkane przez ludność słowiańską ( Kozaków ) , jednak władze ZSRR , zwalczające Kozaków , zachęcały Czeczenów do osiedlania się w mieście .
