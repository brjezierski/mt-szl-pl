Großheirath
Großheirath – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Frankonia , w regionie Oberfranken-West , w powiecie Coburg .
Leży około 9 km na południe od Coburga , nad rzeką Itz , przy autostradzie A73 , drodze B4 i linii kolejowej Coburg – Großheirath .
