Kopalnia ( przystanek kolejowy )
Kopalnia – dawny wąskotorowy przystanek osobowy w Gościeradzu , w gminie Koronowo , w powiecie bydgoskim , w województwie kujawsko-pomorskim .
Położony był na wąskotorowej linii kolejowej z Bydgoszczy do Koronowa .
Linia ta została otwarta w 1895 roku .
W 1969 roku linię zamknięto dla ruchu pasażerskiego .
