Grubale
Grubale – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 9 km na wschód od centrum Siedlec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Mikołaja w Pruszynie .
W Grubalach znajduje się świetlica .
Przez Grubale przebiega droga powiatowa Stok Lacki - Cielemęc .
