Nazwa miesiąca ( według Brücknera ) pochodzi od słowa `` '' ( podobnie ukraiński `` грудень / hrudeń '' , litewski `` gruodis '' ) .
Inna dawniej używana nazwa to `` prosień '' lub `` prosiniec '' ( por .
współczesna nazwa miesiąca w języku czeskim `` prosinec '' i chorwackim `` prosinac '' ) .
Łacińska nazwa `` December '' ( ‘ dziesiąty miesiąc ’ ; zobacz : kalendarz rzymski ) została zapożyczona przez większość języków europejskich .
Pod koniec miesiąca następuje na półkuli północnej przesilenie zimowe , a na południowej przesilenie letnie .
Meteorologicznie jest to w Polsce miesiąc zimowy .
