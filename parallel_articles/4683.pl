Gruzini
Gruzini , nazwa własna : Kartwelowie ( gruz .
ქართველები `` Kartwelebi '' ) − naród kartwelski , zamieszkujący głównie Gruzję ( 3,2 mln ) , a także Turcję ( 168 tys .
) , Rosję ( 157 tys . )
oraz Iran ( 62 tys. , głównie Ferejdunszahr ) .
Posługuje się językiem gruzińskim , który należy do kartwelskiej rodziny językowej .
Dominującym wyznaniem wśród Gruzinów jest prawosławie .
Gruzini wywodzili się z cywilizacji kolchijskiej i iberyjskiej starożytności , Kolchida była połączona ze światem helleńskim , podczas gdy Iberia znajdowała się pod wpływem Imperium Achemenidów , dopóki Aleksander Wielki nie podbił jej .
W IV wieku Gruzini stali się jednymi z pierwszych , którzy przyjęli chrześcijaństwo , a obecnie większość Gruzinów to prawosławni , przy czym większość podąża za narodowym autokefalicznym gruzińskim Kościołem prawosławnym , chociaż istnieje niewielka grupa gruzińskich katolików , gruzińskich społeczności muzułmańskich , a także znacznej liczby niereligijnych Gruzinów .
