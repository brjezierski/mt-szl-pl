Gruzja
Gruzja ( , trb .
`` Sakartwelo '' ) – państwo położone na pograniczu Europy i Azji , w Kaukazie Południowym .
Graniczy na północy z Rosją , na południu z Armenią i Turcją , a na wschodzie z Azerbejdżanem ; zachodnią granicę kraju wyznacza wybrzeże Morza Czarnego .
Stolicą Gruzji jest Tbilisi , przy czym w latach 2012-2018 siedzibą parlamentu było Kutaisi a sądu konstytucyjnego – Batumi .
Gruzja jest członkiem Rady Europy , Organizacji Bezpieczeństwa i Współpracy w Europie , GUAM , Organizacji Współpracy Gospodarczej Państw Morza Czarnego i Eurocontrol .
Od 2014 Gruzja jest krajem stowarzyszonym z Unią Europejską , a od 2016 jest członkiem strefy wolnego handlu z Unią Europejską .
Geografia .
Teren w większej części górzysty .
Północna granica państwa przebiega wzdłuż Pasma Głównego ( Wododziałowego ) Wielkiego Kaukazu – najwyższy szczyt to Szchara ( wierzchołek wschodni ) – 5193 m n.p.m .
Z kolei najbardziej znanym szczytem Gruzji jest Kazbek ( 5033,8 m n.p.m .
) , leżący w pobliżu Gruzińskiej Drogi Wojennej .
Od Pasma Głównego odchodzą na południe ( wymienione w kolejności od zachodu na wschód ) : Góry Gagrińskie , Góry Bzypijskie , Góry Abchaskie , Góry Kodorskie , Góry Swaneckie , Góry Leczchumskie , Góry Raczyńskie , Góry Lichskie , Góry Kartlijskie , Góry Kacheckie , Góry Gomborskie .
Góry Lichskie dzielą państwo na dwie części : wschodnią i zachodnią .
Na południu znajdują się pasma Małego Kaukazu : Góry Adżarsko-Imeretyńskie ( do 2850 m n.p.m .
– Mepisckaro ) , Góry Szawszeckie ( na granicy z Turcją ) , Góry Samsarskie ( Didi Abuli 3300 m n.p.m . )
i Góry Dżawacheckie .
Między Wielkim Kaukazem a Małym Kaukazem rozciągają się obniżenia , z Niziną Kolchidzką na czele .
Zachodnią granicę stanowi wschodni brzeg Morza Czarnego ; linia brzegowa słabo rozwinięta , brzegi na północy i południu urwiste , w środkowej części niskie .
W zależności od przyjętej wersji ( granica Europa-Azja ) całe terytorium Gruzji znajduje się w Europie , lub w Europie i w Azji .
Na terytorium Gruzji panuje klimat podzwrotnikowy , wilgotny w zachodnich regionach i kontynentalny na wschód od Gór Suramskich .
