Gryfice ( stacja kolejowa )
Gryfice – stacja kolejowa w Gryficach , w województwie zachodniopomorskim , w Polsce .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
W 1882 r. Gryfice uzyskały kolejowe połączenie z Goleniowem i Kołobrzegiem ( linia czynna do dziś ) .
14 lat później ( w 1896 r. ) otwarto wąskotorową linię kolejową o szerokości 750 mm łączącą Gryfice z Niechorzem i Dargosławiem ( przez Brojce ) .
3 lata później przebudowano ją na 1000 mm .
W 1903 r. otwarta została końcowa część linii do Stepnicy .
10 lat później połączono Niechorze ( przez Pogorzelicę ) z Trzebiatowem i Dargosławiem .
Od roku 1991 zamykane są kolejne odcinki gryfickiej sieci kolei wąskotorowej , obecnie czynny jest tylko jeden : Gryfice Wąskotorowe – Pogorzelica Gryficka i jest to jedyna czynna linia wąskotorowa w województwie .
