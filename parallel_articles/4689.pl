Kalendarz gregoriański
Kalendarz gregoriański – kalendarz słoneczny wprowadzony w 1582 roku przez papieża Grzegorza XIII bullą `` Inter gravissimas '' ; kalendarz juliański zreformowany przez Luigiego Lilio .
Jest to w zasadzie kalendarz juliański , do którego wprowadzono poprawkę w naliczaniu lat przestępnych , mającą na celu zapobiegnięcie opóźnianiu się kalendarza względem roku zwrotnikowego .
Zniwelowano także część różnicy narosłej od wprowadzenia kalendarza juliańskiego .
Kalendarz juliański spóźnia się o 1 dzień na 128 lat , natomiast opóźnienie kalendarza gregoriańskiego wynosi 1 dzień na 3322 lata .
