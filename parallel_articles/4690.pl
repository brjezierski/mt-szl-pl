Polityka .
Na mocy konstytucji duńskiej z 1953 roku Grenlandia jest integralną częścią Królestwa Danii , posiadającą od 1979 roku szeroką autonomię .
W kwestii duńskich władz centralnych , które na Grenlandii reprezentuje wysoki przedstawiciel , są : sprawy konstytucji , obrony , polityki zagranicznej i monetarnej .
Grenlandia wybiera 2 deputowanych do parlamentu duńskiego ( Folketing ) .
Lokalnym organem władzy ustawodawczej jest Landsting ( kadencja 4 lata , z wyborów powszechnych ) , władzy wykonawczej – rząd , odpowiedzialny przed Landstingiem .
Główne partie polityczne : `` Siumut '' ( „ naprzód ” ) , założone w 1977 roku , socjaldemokratyczna , opowiadająca się za jak najszerszą autonomią w ramach Danii , `` Atassut '' ( „ poczucie wspólnoty ” ) , zał .
1978 , konserwatywno-liberalna , popiera bliskie związki z Danią , `` Inuit Ataqatigiit '' ( „ wspólnota ludzka ” ) , zał .
1978 , inuicka partia niepodległościowa .
25 listopada 2008 roku odbyło się referendum w sprawie poszerzenia autonomii .
75,54 % mieszkańców głosowało za autonomią , przeciw było 23,57 % , frekwencja wyniosła 71,96 % .
W wyniku referendum przekazano rządowi Grenlandii sprawy polityki morskiej , zasobów naturalnych oraz policję i sądownictwo , łącznie ok. 30 obszarów .
Język grenlandzki został jedynym językiem urzędowym .
Nie zmieniła się polityka zagraniczna i obronna .
Nowe przepisy dotyczące poszerzenia autonomii weszły w życie 21 czerwca 2009 r .
USA mają na Grenlandii bazę wojsk powietrznych Thule z elementami systemu wczesnego ostrzegania przed pociskami balistycznymi ; bazę tę wykorzystuje również dowództwo sił kosmicznych .
W czasie zimnej wojny działała też tajna baza Camp Century .
Początki tej obecności sięgają 9 kwietnia 1941 r. , gdy USA przejęły kontrolę nad wyspą prawdopodobnie jeszcze bez oficjalnej zgody duńskiego rządu emigracyjnego .
W XIX w. Departament Stanu USA analizował możliwość zakupu Grenlandii i Islandii , gdy w 1867 r. sekretarz stanu William H. Seward zlecił ocenę możliwości takiej transakcji , ale nie złożono Danii żadnej propozycji .
W 1946 r. prezydent Harry Truman bezskutecznie złożył Danii ofertę zakupu Grenlandii za 100 mln dol. , a w 2019 r. do pomysłu wrócił prezydent Donald Trump , ale do pomysłu sceptycznie odniosło się wielu duńskich polityków .
Sport .
Grenlandia ma własną zawodową reprezentację piłkarzy ręcznych .
Istnieje także liga piłkarska Coca Cola GM i nieoficjalna reprezentacja w piłce nożnej .
Grenlandię reprezentują sportowcy także w biathlonie .
Media .
Na Grenlandii głównym dostawcą usług telekomunikacyjnych i internetowych jest należąca do rządu spółka TELE Greenland .
Według statystyk ponad 90 % mieszkańców wyspy korzysta z internetu .
Grenlandia ma własną korporację radiowo-telewizyjną , `` Kalaallit Nunaata Radioa '' , która większość programów nadaje w języku grenlandzkim .
