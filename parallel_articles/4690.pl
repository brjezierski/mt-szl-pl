Samobójstwa :
Według różnych doniesień do pierwszej połowy XX wieku Grenlandia należała do terytoriów z niewielką liczbą samobójstw .
Jednak od roku 1970 ich liczba zaczęła gwałtownie rosnąć i w 1986 była w niektórych miejscowościach główną przyczyną zgonów wśród młodych ludzi .
Według niektórych raportów przeciętnie co 4 lub co 5 Grenlandczyk podejmował próbę odebrania sobie życia .
Najczęściej młodzi ludzie w wieku przeciętnie od 15 do 19 lat .
Wśród przyczyn wymienia się alkoholizm i depresję .
Zasadniczą przyczyną może być zmiana stylu życia Grenlandczyków , którzy w ramach tzw .
polityki G60 byli przesiedlani do bloków w tzw .
„ stylu sowieckim ” .
Ludzie ci , którzy przez stulecia zajmowali się łowiectwem , nagle znaleźli się w zupełnie nowej sytuacji , w której nie potrafią się odnaleźć .
Flora .
Flora Grenlandii jest uboga .
Na jej terenie spotkać można ok. 500 gatunków roślin wyższych oraz 3000 gatunków mchów , porostów i glonów .
Roślinność na Grenlandii występuje tylko w miejscach , które choć przez krótki czas w ciągu roku są wolne od pokrywy lodowej .
Zasadniczy typ roślinności porastającej te obszary stanowią różne typy tundry oraz łąki .
Tundra na południu złożona jest z różnych gatunków traw , mchów , porostów i roślin zielnych , a na północy tylko z mchów i porostów .
Na samym południu terytorium występują także krzewy i karłowate gatunki drzew , m.in .
brzozy ( osiągające 10 m wysokości ) , olchy , wierzby ( sięgające 4 m ) , jarzębiny , jałowce .
Północne wybrzeże ( powyżej 80° szerokości geograficznej ) jest praktycznie pozbawione roślinności .
