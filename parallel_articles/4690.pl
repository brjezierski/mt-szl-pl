Na Grenlandii żyje ok. 700 gatunków owadów i pajęczaków : liczne komary , motyle , trzmiele i pająki .
Klimat .
Na Grenlandii panuje klimat polarny , jedynie na wybrzeżach – subpolarny .
Południowy zachód wyspy jest cieplejszy niżby wynikało z szerokości geograficznej , a to za sprawą opływającego wyspę od tej strony odgałęzienia ciepłego Prądu Północnoatlantyckiego .
Klimat Grenlandii charakteryzuje się częstymi zmianami pogody , ostrymi zmianami temperatur i znacznymi opadami ( zwłaszcza w części południowej ) .
Na zjeździe Amerykańskiej Unii Geofizycznej w 2007 roku niektórzy badacze , m.in .
Thomas V. Lowell z uniwersytetu w Cincinnati i 6 innych , przedstawili wyniki swoich prac , z których wynika , że w okolicach fiordu Kangertittivaq ( Scoresbysund ) , w pobliżu miejscowości Ittoqqortoormiit , znajdują się odsłonięte przez ustępujący lodowiec Istorvet duże ilości pozostałości roślinnych – pni i korzeni drzew i krzewów itp .
Na podstawie dokonanych odkryć uważa się , że w okresie tzw .
„ średniowiecznego okresu ciepłego ” klimat na Grenlandii był na tyle korzystny , że umożliwiał bujną wegetację drzew i roślin na znacznej powierzchni wyspy .
Temperatura .
Najkorzystniejszy klimat panuje na wybrzeżach , zwłaszcza w południowo-zachodniej części wyspy .
Średnia temperatura w tym rejonie w styczniu wynosi −7 °C , zaś lipcu +10 °C .
Pozostałe obszary wyspy leżące nad brzegiem morza są znacznie chłodniejsze : temperatura spada w miarę przesuwania się na północ ; zimą na północnych skrajach wyspy osiąga wartość średnią −36 °C , a latem +3 °C
Najsurowszy klimat panuje w środkowej części Grenlandii .
Temperatura na tym obszarze w zasadzie nigdy nie przekracza 0 °C .
Średnia temperatura powietrza w lutym wynosi tam −47 °C , zaś w lipcu −12 °C .
Na Grenlandii odnotowano rekordy niskiej temperatury dla półkuli zachodniej uznane przez Światową Organizację Meteorologiczną .
9 grudnia 1954 na stacji North Ice zanotowano –66,1 °C .
22 grudnia 1991 na automatycznej stacji pogodowej Klinck , leżącej w centralnej części wyspy w pobliżu jej najwyżej położonego punktu , –69,6 °C .
Ten ostatni wynik jest również uznany za rekordowo niską wartość dla półkuli północnej .
Wzrost średniej temperatury w ostatnich latach , powoduje wydłużenie okresu bezprzymrozkowego .
W związku z tym podjęto próbę uprawy jęczmienia .
Opady .
Roczna suma opadów na wyspie waha się od 800 do 1100 mm na południu , do 150–250 mm na północy .
Opady mają głównie postać śniegu , w okresie letnim też deszczu , zaś w środkowej części Grenlandii , na obszarze lodowca , gdzie rocznie spada 300–400 mm , jest to wyłącznie śnieg .
Opady występują głównie w ciepłej połowie roku .
Fala upałów , która przetoczyła się przez północną półkulę w połowie sierpnia 2021 , spowodowała pierwszy znany przypadek opadów deszczu na szczycie grenlandzkiego lodowca .
Zmiana klimatu .
W latach od 1989 do 1993 , Amerykańscy i Europejscy klimatolodzy wywiercili na szczycie Grenlandii dwa rdzenie lodowe o długości 3 km .
Analizy warstw i ich składu chemicznego dostarczyły rewolucyjnych danych na temat zmian klimatycznych na północnej półkuli .
Rdzenie pozwalają na cofnięcie się o ok. 100 000 lat i ilustrują , że pogoda i temperatura często zmieniały się gwałtownie pomiędzy pozornie stabilnymi stanami , wywołując globalne konsekwencje .
Współcześnie wzrost średniej temperatury oceanu oraz atmosfery ( związane ze zjawiskiem globalnego ocieplenia ) powodują nasilony spływ lodu z głębi lądolodu oraz wzmożone topnienie powierzchniowe , czego efektem jest ujemny bilans masy Grenlandii .
Lodowce Grenlandii przyczyniają się do wzrostu poziomu morza szybciej niż wcześniej szacowano : w latach 2006–2016 Grenlandia traciła średnio rzecz biorąc 278±11 Gt lodu rocznie , co opowiadało wzrostowi średniego poziomu morza o 0,77±0,05 mm rocznie ( dane z lat 2006–2015 ) .
Spadek całkowitej masy lądolodu nie wyklucza regionalnych i okresowych wzrostów jego grubości : przykładowo w latach 1994–2005 większe opady śniegu wynikające z oscylacji północnoatlantyckiej spowodowały przyrost wewnętrznej pokrywy lodowej o średnio 6 cm .
Emisja gazów cieplarnianych .
Emisja równoważnika dwutlenku węgla z Grenlandii wyniosła w 1990 roku zaledwie 0,025 Mt , z czego większość wynosiła emisja metanu , a tylko 0,003 Mt stanowiła emisja dwutlenku węgla .
W przeliczeniu na mieszkańca emisja wyniosła wówczas 55 kg dwutlenku węgla , a w przeliczeniu na 1000 dolarów PKB 2 kg .
Poziom ten był praktycznie niezmienny aż do skokowego wzrostu w 2004 .
W 2005 emisja dwutlenku węgla wyniosła już 0,631 Mt , będąc odpowiedzialną za prawie całą emisję równoważnika dwutlenku węgla .
Głównym źródłem emisji była emisja z budynków , podczas gdy emisja z przemysłu energetycznego i transportu jest trochę mniejsza .
Poziom emisji od tego czasu waha się i w 2018 emisja dwutlenku węgla pochodzenia kopalnego wyniosła 0,529 Mt , a w przeliczeniu na mieszkańca 9,353 t i w przeliczeniu na 1000 dolarów PKB 172 kg .
