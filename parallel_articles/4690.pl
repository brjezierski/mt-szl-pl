Stolicą Grenlandii jest Nuuk ( duńska nazwa `` Godthåb '' ) .
W 1979 roku duński parlament przyznał Grenlandii autonomię .
W 1982 roku odbyło się referendum , w którym mieszkańcy ( 52 % ) opowiedzieli za wystąpieniem z Europejskiej Wspólnoty Gospodarczej .
Grenlandia wystąpiła z niej w 1985 roku .
Grenlandia w przeciwieństwie do Danii nie jest członkiem Unii Europejskiej .
Grenlandia jest członkiem Rady Nordyckiej .
W 2008 roku odbyło się referendum , w wyniku którego autonomia Grenlandii została poszerzona .
Nazwa wyspy oznacza „ zielony kraj ” – miała zachęcić do osiedlania się na niej , w przeciwieństwie do Islandii , której nazwa „ kraina lodu ” miała zniechęcać .
Geografia .
Wyspa leży w północnej części Oceanu Atlantyckiego , jest częścią Ameryki Północnej i największą na świecie niekontynentalną wyspą .
Od reszty kontynentu oddzielają ją cieśniny Davisa , Smitha i Robesona oraz Morze Baffina .
Najdalej na północ położonym punktem jest Przylądek Morris Jesup , a na południe Przylądek Farvel .
Od wschodu Cieśnina Duńska oddziela ją od Islandii .
Najwyższym szczytem jest Góra Gunnbjørna 3700 m n.p.m. , grubość lodu w czaszy lodowca dochodzi do 3500 m. Wybrzeże typu fiordowego z licznymi przybrzeżnymi wyspami .
Większa część Grenlandii leży na północ od koła podbiegunowego .
Na Grenlandii znajduje się również największy na świecie park narodowy .
Gdyby cała pokrywa lodowa Grenlandii uległa stopnieniu , poziom mórz podniósłby się o ponad 7 m .
Ludność .
Przeważająca część ludności Grenlandii zamieszkuje południowo-zachodnią część wyspy , gdzie warunki klimatyczne są najkorzystniejsze .
Około 40 % stanowią mieszkańcy licznych osad lub niewielkich miejscowości , położonych wokół wybrzeży Grenlandii .
Około 25 % całej populacji mieszka w stolicy terytorium – Nuuk .
Narodowości .
Większość mieszkańców Grenlandii stanowią Grenlandczycy ( Inuici ) – 89 % .
Zamieszkują ją również Europejczycy o różnym stopniu wymieszania , głównie Duńczycy .
Języki .
Mieszkańcy Grenlandii posługują się trzema dialektami języka grenlandzkiego , którego forma używana na zachodnim wybrzeżu została językiem urzędowym .
Powszechna jest również znajomość duńskiego oraz , w mniejszym stopniu , angielskiego ( głównie w miastach ) .
