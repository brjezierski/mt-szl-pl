Grenlandia
Grenlandia ( gren .
`` Kalaallit Nunaat '' [ ] , [ ] ) – autonomiczne terytorium zależne Danii położone na wyspie o tej samej nazwie , która położona jest geograficznie w Ameryce Północnej , a historycznie i politycznie w Europie , o obszarze 2 166 086 km² ( największa wyspa na świecie niebędąca kontynentem ) , pokrytej w 81 % przez lądolód grenlandzki ( 410,4 tys .
