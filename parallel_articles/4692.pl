Gręzów
Gręzów – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Kotuń .
Leży przy drodze krajowej nr , w odległości około 10 km na zachód od Siedlec .
Nazwa pochodzi najprawdopodobniej od grzęzawisk , które znajdowały się dawniej na terenie wsi .
Dużą część Gręzowa zajmują lasy ( na zachodzie i północy ) a także żwirownie , na wschodzie luźna zabudowa mieszkalna , a w środkowej części znajduje się skupisko domów , w Gręzowie działa kilka pieczarkarni .
Gręzów posiada dobrze rozwiniętą komunikację .
W miejscowości znajduje się 5 przystanków autobusowych i pętla autobusowa MPK Siedlce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W Gręzowie działa prężnie zespół taneczny `` Active Dance '' , Koło Gospodyń Wiejskich oraz zespół pieśni ludowych `` Gręzowianki '' .
W 2012 roku , z inicjatywy młodzieży z zespołu Active Dance , została zapoczątkowana tradycja wakacyjnego Koncertu Charytatywnego , podczas którego zbierane są fundusze na leczenie chorych dzieci z gminy .
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Nieustającej Pomocy w Starym Opolu .
