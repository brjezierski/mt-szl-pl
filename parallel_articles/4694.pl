Gryżów
Gryżów ( ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
Historia .
Wieś wzmiankowana była po raz pierwszy w XIV w. Obok wsi , na terenie Piorunkowic , nikłe ślady zamku obronnego „ Gryżów ” , zniszczonego w czasie powstania husyckiego .
W 1560 r. tutejsi chłopi sprzeciwili się włączeniu ich gruntów w skład nyskiego księstwa biskupiego .
Przywódców buntu uwięziono , a następnie wygnano z terenów `` państwa biskupiego '' .
