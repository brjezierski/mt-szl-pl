Grzawa
Grzawa ( pierwotnie `` Rdzawa '' ) – wieś w Polsce położona w województwie śląskim , w powiecie pszczyńskim , w gminie Miedźna .
Nazwa .
Grzawa jest jedyną w Polsce miejscowością o takiej nazwie .
Gdyby w przeszłości jej pisownia nie przechodziła różnych przeinaczeń , dziś wioska zwałaby się zapewne Rdzawą ( 1563 , a także 1524 , 1568 , 1598 , później `` Grdzawa '' – 1444 , 1772 , 1790 , `` Rzawa '' rok 1517 , `` Gzawa '' rok 1536 , `` Grdziawa '' rok 1636 , także 1721 ) .
Ongiś było to określenie koloru wody zabarwionej związkami żelaza .
