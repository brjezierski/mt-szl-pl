W 2012 roku kandydował na radnego Bytomia w przedterminowych wyborach z listy Ruchu Autonomii Śląska .
Działalność i publikacje .
Swoje teksty publikował w śląskim portalu Wachtyrz.eu , od 2016 roku na portalu YouTube prowadzi kanał `` Chwila z gŏdkōm '' , poświęcony językowi śląskiemu , pisał bloga pod adresem poslunsku.eu , od 2016 tworzył bloga pod adresem grzegorzkulik.pl .
Napisał śląską lokalizację gry Euro Truck Simulator 2 , śląskie napisy do filmu `` '' , śląskie lokalizacje WordPressa i Firefoksa .
Stworzył skrypt śląskiej wersji Facebooka , wyszukiwarki Google oraz serwisu YouTube .
Opracował śląską klawiaturę dla systemów Microsoft Windows i Linux .
W 2016 roku napisał śląski korektor tekstu .
W swoich wystąpieniach publicznych wypowiada się wyłącznie po śląsku .
