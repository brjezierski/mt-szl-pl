Życiorys .
Pochodzi ze śląskiej rodziny ; jego przodkowie mieszkali w XVIII wieku w Rokitnicy .
W latach 1998–2002 uczył się w Liceum Ogólnokształcącym im .
Powstańców Śląskich w Radzionkowie .
W latach 2002–2004 uczęszczał do Studium Obsługi Ruchu Turystycznego w Zespole Szkół Gastronomiczno-Hotelarskich w Bytomiu .
Odbył służbę wojskową w 1 Warszawskiej Brygadzie Pancernej im .
Tadeusza Kościuszki w 2005 roku .
Od 2006 roku studiował marketing polityczny i dziennikarstwo w Wyższej Szkole Ekonomii i Administracji w Bytomiu , po pięciu semestrach przerwał studia .
