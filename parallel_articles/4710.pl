Grąbków
Grąbków – wieś w Polsce położona w województwie wielkopolskim , w powiecie tureckim , w gminie Malanów .
Wieś arcybiskupstwa gnieźnieńskiego w powiecie sieradzkim województwa sieradzkiego w końcu XVI wieku .
Sołtysem miejscowości jest Daniel Szymański , zaś w skład rady sołeckiej wchodzą także Adam But , Paweł Przygoński , Stanisław Różański oraz Roman Kubiak .
Historia .
`` Słownik Geograficzny Królestwa Polskiego '' informuje , że `` jest to kolonia i folwark w powiecie tureckim , w gminie i parafii Malanów , odległa o siedem wiorst od Turku '' .
W 1827 roku było tutaj 48 domów , zamieszkanych przez około 400 osób .
Autorzy Słownika wspominają , że Grąbków występował także jako `` Grombków '' , a nawet `` Grunkowo '' .
W 1872 roku tutejszy majątek nabył za 36 tysięcy rubli niejaki J. Bednarkiewicz .
Wówczas majątek składał się z folwarków Grąbków i Paździerowice , wsi o tych nazwach oraz atencyi Józefina .
Jego powierzchnia wynosiła 995 mórg .
Folwark Grąbków liczył 752 morgi ( z czego 407 mórg gruntów ornych i ogrodów , 90 łąk , 241 lasu , 14 nieużytków i placów ) .
