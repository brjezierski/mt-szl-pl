Guam
Guam ( Terytorium Guamu ) – wyspa w południowej części archipelagu Marianów w Mikronezji , terytorium nieinkorporowane Stanów Zjednoczonych , ze stolicą w Hagåtña .
Ustrój polityczny .
Guam jest terytorium zależnym od Stanów Zjednoczonych .
Zarządza nim amerykański Departament Spraw Wewnętrznych .
Na mocy ustawy z 1950 roku ( Guam Organic Act of 1950 ) terytorium posiada autonomię wewnętrzną .
Władzę wykonawczą sprawuje gubernator wybierany w wyborach powszechnych co 4 lata .
Władzę ustawodawczą sprawuje 15-osobowy parlament ( Legislature of Guam ) , pochodzący z odbywających się co 2 lata wyborów powszechnych .
Prawo głosu mają obywatele w wieku powyżej 18 lat .
Guamczycy są obywatelami USA , jednak nie mogą głosować w wyborach prezydenckich .
Główne partie polityczne :
Święto narodowe : Dzień Odkrycia Guamu , pierwszy poniedziałek marca ( 1521 ) .
Geografia .
Wyspa pochodzenia wulkanicznego , otoczona rafami koralowymi , położona na północnym Pacyfiku , pomiędzy Hawajami a Filipinami .
Jest to największa ( 541,3 km² ) i najdalej wysunięta na południe wyspa archipelagu Marianów .
