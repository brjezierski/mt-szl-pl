Guanajuato ( miasto )
Guanajuato – miasto w środkowym Meksyku , na obszarze Mesy Centralnej , na wysokości 2050 metrów , stolica stanu Guanajuato .
Miasto zostało założone na początku XVI wieku przez Hiszpanów .
Podstawą gospodarki miasta od XVIII w. były kopalnie rud srebra .
W szczytowym okresie wydobycia z Guanajuato pochodziło 2/3 światowej produkcji tego metalu .
Pozostałością po górniczej przeszłości jest głęboki na 600 metrów szyb górniczy .
