Guantánamo ( zatoka )
Guantánamo ( ang .
`` Guantánamo Bay '' , hiszp .
`` Bahía de Guantánamo '' ) – zatoka w prowincji Guantánamo na południowym wschodzie Kuby .
Część zatoki znajduje się od 1903 roku pod administracją USA ( Baza wojskowa Guantanamo ) , pomimo że rząd kubański twierdzi , iż teren jest okupowany przez USA , argumentując swoje stanowisko tym , że umowa zawarta w 1903 roku między Kubą a USA została zawarta niezgodnie z prawem .
Nazwę zatoce nadali Tainowie , Krzysztof Kolumb nazwał ją `` Puerto Grande '' .
