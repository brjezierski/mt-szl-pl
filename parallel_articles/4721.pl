Guasave
Guasave – miasto w zachodniej części meksykańskiego stanu Sinaloa , położone w odległości około 30 kilometrów od wybrzeża Pacyfiku nad Zatoką Kalifornijską , na południowy wschód od miasta Los Mochis .
Miasto w 2005 roku liczyło 66 793 mieszkańców .
Nazwa miasta i gminy pochodzi od nazwy koczowniczego plemienia ( Guasaves oraz Tamasulas ) zamieszkujących w prehiszpańskiej historii tereny gminy Guasave oraz sąsiedniej Angostura .
Gmina Guasave .
Miasto jest siedzibą władz gminy Guasave , jednej z 18 gmin w stanie Sinaloa .
