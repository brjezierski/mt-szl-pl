Guastalla
Guastalla – miejscowość i gmina we Włoszech , w regionie Emilia-Romania , w prowincji Reggio Emilia .
Jest głównym miastem Bassa Reggiana i znajduje się w niewielkiej odległości od prawego brzegu Padu , ok. 30 km od Reggio nell'Emilia , Parmy i Mantui .
Historia .
Znaleziska archeologiczne wskazują , że miejscowość była zamieszkana już w czasach etruskich .
Odnalezione fragmenty zdobionych naczyń , przedmiotów metalowych i innych elementów znalezionych w grobach , wskazują na istnienie tu zorganizowanej społeczności .
