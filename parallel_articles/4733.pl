Guerrero
Guerrero – stan położony w południowo-zachodnim Meksyku nad Oceanem Spokojnym .
Graniczy ze stanami Michoacán , México , Morelos , Puebla oraz Oaxaca .
Stolicą stanu jest miasto Chilpancingo .
Podział administracyjny .
Guerrero dzieli się na 77 gmin ( ) .
Historia .
Najstarsze ślady obecności człowieka na tych ziemiach pochodzą z ok. 300 r. p.n.e .
Wtedy w środkowym i południowym Meksyku rozwijała się cywilizacja Olmeków .
W X w. w ludy z kręgu kultury Teotihuacan wzniosły na tym terenie , a dokładniej w Texmeluncanie i Teloloapanie piramidy służące celom sakralnym .
W Guerrero na wybrzeżu Pacyfiku osiedlali się także Tepanekowie i Indianie z innych plemion .
W XI w. z północy przybyli na już wówczas gęsto zaludnione tereny Aztekowie .
