Guilberville
Guilberville – miejscowość i dawna gmina we Francji , w regionie Normandia , w departamencie Manche .
W 2013 roku jej populacja wynosiła 1094 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia czterech ówczesnych gmin – Brectouville , Giéville , Guilberville oraz Torigni-sur-Vire – utworzono nową gminę Torigny-les-Villes .
Siedzibą gminy została miejscowość Torigni-sur-Vire .
