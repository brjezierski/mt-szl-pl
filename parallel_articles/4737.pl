Guillermo Lasso
Guillermo Alberto Santiago Lasso Mendoza ( ur .
16 listopada 1955 w Guayaquil ) – ekwadorski polityk i ekonomista , gubernator prowincji Guayas w latach 1998–1999 , minister gospodarki w 1999 roku , od 2021 roku prezydent Ekwadoru .
Życiorys .
Młodość i edukacja .
Guillermo Lasso urodził się jako najmłodsze z jedenaściorga dzieci .
Jego ojciec , Enrique Lasso Alvarado , był funkcjonariuszem publicznym w Guayaquil .
Z powodów ekonomicznych w czasie nauki w szkole średniej podjął pracę w niepełnym wymiarze w kilku firmach finansowych .
W 1974 ukończył Colegio San José La Salle i rozpoczął studia na Wydziale Ekonomicznym Pontificia Universidad Católica del Ecuador w Quito .
Zrezygnował jednakże z nauki na drugim roku studiów , by rozpocząć pracę w sektorze prywatnym .
W 1981 poślubił Maríę de Lourdes Alcívar Crespo , z którą ma pięcioro dzieci .
Praca w bankach .
W 1977 rozpoczął pracę w firmie ProCrédito .
Rok później założył wspólnie z bratem własną firmę Constructora Alfa y Omega .
W 1980 został zastępcą dyrektora spółki Finansur , a w 1984 jej dyrektorem .
W 1989 objął funkcję wiceprezesa banku Banco de Guayaquil , a pięć lat później funkcję prezesa , którą pełnił do maja 2012 .
Kariera polityczna .
Po objęciu urzędu przez prezydenta Jamila Mahuada , 10 sierpnia 1998 został mianowany gubernatorem prowincji Guayas .
Po roku , 17 sierpnia 1999 objął nowo utworzony urząd superministra gospodarki , koordynujący prace resortów finansów i energii .
24 września 1999 podał się do dymisji , w proteście przeciwko decyzji prezydenta Mahuada o uchwaleniu moratorium na spłatę zadłużenia zagranicznego Ekwadoru .
W 2011 otrzymał tytuł doctor honoris causa od Universidad de las Américas w Quito .
Do aktywności politycznej powrócił w 2012 , kiedy zdecydował się na udział w wyborach prezydenckich .
W maju 2012 zrezygnował z funkcji prezesa Banco de Guayaquil , w sierpniu 2012 wstąpił do nowo utworzonego ruchu politycznego Tworzenie Możliwości , a w październiku 2012 został jego kandydatem na urząd prezydenta .
W wyborach prezydenckich 17 lutego 2013 zajął drugie miejsce , zdobywając 22,7 % głosów i przegrywając z urzędującym prezydentem Rafaelem Correą ( zdobył 57,2 % głosów ) .
Po raz drugi wziął udział w wyborach prezydenckich w 2017 .
Opowiadał się za niezależnością banku centralnego od władzy wykonawczej oraz zapowiedział usunięcie z ekwadorskiej ambasady w Londynie przebywającego tam założyciela serwisu WikiLeaks , Juliana Assange ’ a .
W pierwszej turze wyborów 19 lutego 2017 zajął drugie miejsce z wynikiem 28,1 % głosów , przegrywając z Lenínem Moreno ( 39,3 % ) .
Ponieważ żaden z kandydatów nie zdobył ponad 40 % głosów , z co najmniej 10-procentową przewagą nad kontrkandydatem , 2 kwietnia 2017 Moreno i Lasso zmierzyli się w drugiej turze wyborów .
Lasso przegrał w niej z Morenem , uzyskując 48,85 % głosów .
Po tym jak sondaże exit poll wskazywały początkowo na jego wygraną , domagał się ponownego przeliczenia głosów , a jego zwolennicy protestowali na ulicach stolicy .
W pierwszej turze wyborów prezydenckich w 2021 roku zajął drugie miejsce .
W drugiej turze wyborów pokonał socjalistę Andresa Arauza stosunkiem głosów 52,36–47,64 % .
Poglądy polityczne .
W 2021 roku jego kampania wyborcza opierała się na powrocie do wolnego rynku , ochronie środowiska naturalnego , a także praw osób LGBT .
Określa się jako liberał , jest zwolennikiem wolnego rynku .
