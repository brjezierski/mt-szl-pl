Guimarães
Guimarães ( wym .
[ ] ) – miasto w Portugalii , leżąca w dystrykcie Braga , w regionie Północ w podregionie Ave. Jest siedzibą gminy o tej samej nazwie .
Historia .
Miasto zostało założone przez Vimarę Peresa – pierwszego hrabiego Portucale i nazwane od jego imienia `` Vimaranis '' , obecnie Guimarães .
Było pierwszą stolicą hrabstwa , pełniąc ważną funkcję w pierwszym okresie państwowości portugalskiej .
