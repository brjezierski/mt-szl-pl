Gujana
Gujana ( ang .
`` Guyana '' ) , oficjalnie Kooperacyjna Republika Gujany ( ang .
`` Co-operative Republic of Guyana '' ) – państwo w Ameryce Południowej , nad Oceanem Atlantyckim , graniczące na północnym zachodzie z Wenezuelą ( 743 km ) , na południowym zachodzie i południu z Brazylią ( 1119 km ) , a na wschodzie z Surinamem ( 600 km ) .
Przebieg granicy Gujany z Wenezuelą i Surinamem jest kwestionowany przez oba te państwa .
Geografia .
Gujana składa się z trzech regionów geograficznych różniących się ukształtowaniem terenu .
Najwyższa część kraju leży na obszarze Wyżyny Gujańskiej porozcinanej na osobne masywy wieloma dolinami rzek .
W jednym z łańcuchów górskich , na połączeniu granic z Wenezuelą i Brazylią , znajduje się najwyższy szczyt kraju Roraima ( 2810 m n.p.m .
) .
Wyżyna obniżając się w kierunku oceanu tworzy równinne i pagórkowate tereny porośnięte lasami równikowymi , a wzdłuż rzek , galeriowymi .
Lasy zajmują powierzchnię około 80 % kraju .
Dobrze rozwinięta sieć rzek , z których największa to Essequibo , tworzy na tym obszarze , jak również na wyżynach , liczne wodospady .
Nad oceanem rozciąga się pas płaskich , błotnistych nizin , sięgających od 8 do 65 km w głąb lądu .
Wybrzeże porastają lasy namorzynowe .
Część terenów stanowią depresje zabezpieczone systemem kanałów i tam .
Gujanę charakteryzuje klimat równikowy z wysoką temperaturą powietrza przez cały rok .
Temperatura na wybrzeżu waha się od 20 do 26,8 °C , a w głębi kraju od 18 do 39 °C .
Klimat wybrzeża łagodzony jest przez północno-wschodnie wiatry .
Opady występują sezonowo w okresie maj-czerwiec oraz grudzień-styczeń .
Średnia roczna suma opadów waha się od 1200 mm na wyżynie do 3000 mm na wybrzeżu .
Wody .
Sieć rzeczna .
Sieć rzeczna Gujany jest gęsta i dobrze rozwinięta .
Najdłuższa rzeka Gujany to Essequibo ( ok. 1000 km ) , uchodząca szeroką deltą do Oceanu Atlantyckiego .
Na jej lewych dopływach znajdują się liczne wodospady , z których najwyższy jest Kaieteur na rzece Potaro ( 225 m ) .
Do większych rzek Gujany należą również : Mazaruni , Cuyuni , Berbice oraz Courantyne .
Gleby .
W strefie wilgotnych lasów równikowych rozwinęły się gleby czerwonożółte ( ferralsole ) o bardzo niskiej zawartości próchnicy .
Na sawannach wykształciły się czerwone gleby ferralitowe , a wzdłuż dolin dużych rzek – gleby aluwialne ( fluwisole ) .
