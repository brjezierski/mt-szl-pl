Taphrina sadebeckii
Taphrina sadebeckii Johanson – gatunek grzybów z klasy szpetczaków ( `` Taphrinomycetes '' ) .
Grzyb mikroskopijny , pasożyt olchy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Taphrina , Taphrinaceae , Taphrinales , Taphrinomycetidae , Taphrinomycetes , Taphrinomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1886 r. Carl Johan Johanson .
Synonim : `` Taphrina sadebeckii '' var .
`` borealis '' Johanson 1885 .
Morfologia i objawy porażenia .
