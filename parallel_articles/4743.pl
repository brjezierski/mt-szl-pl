Korki wkręcane zaprojektował założyciel firmy Adidas – Niemiec , Adolf `` Adi '' Dassler .
Początkowo obuwie było sztywne i niewygodne , a 3 paski ( logo Adidasa ) służyły do podtrzymania konstrukcji buta .
W 1947 r. szwajcarska firma zaczęła stosować w miejsce `` krążków '' ( korków ) skórzanych , wykonane z gumy .
Ponadto zmodyfikowano konstrukcję butów , tak by były one lżejsze i bardziej elastyczne .
W 1954 roku piłkarska reprezentacja Niemiec wygrała mistrzostwa świata , grając w butach z wkręcanymi korkami , które pozwalały na grę w różnych warunkach bez poślizgów .
