Korek ( but piłkarski )
Korkotrampki – buty , na których podeszwie znajdują się korki ( kolce ) .
Służą one do zwiększenia przyczepności do murawy .
Korki znajdujące się na podeszwie nie mogą być ostre , gdyż zwiększałoby to znacznie możliwość kontuzji piłkarza , na którym dokonywany jest wślizg .
Wyróżnia się cztery rodzaje korkotrampków : `` korki '' , `` lanki '' , `` turfy '' i `` wkręty '' .
