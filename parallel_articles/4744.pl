Gołąbek niemiły
Gołąbek niemiły ( `` Russula subfoetens '' W.G .
Sm . )
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Russula , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Synonimy naukowe :
Nazwę polską nadała Alina Skirgiełło w 1991 r .
Przez długi czas `` Russula subfoetens '' uważany był za odmianę gołąbka śmierdzącego ( `` Russula foetens '' ) , obecnie jednak przez Index Fungorum uważany jest za odrębny i samodzielny gatunek .
Morfologia .
Średnica 4–9 cm , młody półkulisty , później płaski , w środku zagłębiony .
Początkowo ma barwę jasnoochrową , później jest coraz ciemniejszy , brudnordzawy , szczególnie na środku , brzegi pozostają jaśniejsze .
Brzeg zazwyczaj jest silnie karbowany , płatowaty i gruzełkowaty ( zwłaszcza u starszych okazów ) .
Powierzchnia naga , śluzowata , podczas suchej pogody błyszcząca .
Skórka jest promieniście porysowana i daje się ściągnąć do połowy promienia kapelusza .
