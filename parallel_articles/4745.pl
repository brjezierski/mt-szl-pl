Wrośniaczek sosnowy
Wrośniaczek sosnowy ( `` Diplomitoporus flavescens '' ( Bres . )
Domański ) – gatunek grzybów z rzędu żagwiowców ( `` Polyporales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Dichomitus , Incertae sedis , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1903 r. G. Bresàdola nadając mu nazwę `` Trametes flavescens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu S. Domański w 1965 r .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 1999 r , wcześniej S. Domański używał nazwy podskórnik płowiejący .
