Gulu
Gulu – miasto w północnej Ugandzie , w dystrykcie Gulu .
Według danych szacunkowych na rok 2011 liczy 154.300 mieszkańców .
Ośrodek handlowy regionu uprawy bawełny , kawowca i herbaty ; przemysł bawełniany , odzieżowy , spożywczy , zapalczany ; lotnisko ; obozy dla uchodźców z Sudanu , Rwandy i Demokratycznej Republiki Konga .
Drugie co do wielkości miasto kraju .
Stolica archidiecezji Gulu .
