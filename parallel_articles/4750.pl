Gustave Eiffel
Gustave Eiffel , właśc .
Alexandre Gustave Eiffel [ ] ( ur .
15 grudnia 1832 w Dijon , zm .
27 grudnia 1923 w Paryżu ) – francuski inżynier , architekt , konstruktor mostów , wiaduktów , śluz i innych konstrukcji stalowych oraz żeliwnych .
Życie i twórczość .
Urodził się jako Alexandre Gustave Bonickhausen dit Eiffel ; jego przodkowie pochodzili z okolic gór Eifel pomiędzy Akwizgranem , Kolonią i Trewirem .
Uczęszczał do École Centrale de Paris , gdzie studiował chemię .
Po studiach zainteresował się metalurgią i podjął pracę w zakładzie Charles Nepveu , który produkował maszyny parowe i konstrukcje dla kolejnictwa .
Rok później został szefem biura konstrukcyjnego w firmie Pauwels et Cie. , żeby w wieku 25 lat być budowniczym mostu w Bordeaux - ( zwany `` Passerelle Eiffel '' , lub `` Pont ferroviaire Saint-Jean '' ) .
W 1866 zakłada w Paryżu własną firmę .
Wybudował wiele różnych obiektów , które rozsławiły jego nazwisko we Francji i na świecie .
W latach 1870–1880 Eiffel znajdował się u szczytu popularności .
Był wolnomularzem .
