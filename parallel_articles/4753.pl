Guszczewina
Guszczewina ( białorus .
Гушчэвіна ) – wieś w Polsce , położona w województwie podlaskim , w powiecie hajnowskim , w gminie Narewka .
W latach 1975–1998 wieś administracyjnie należała do województwa białostockiego .
Historia .
Według Pierwszego Powszechnego Spisu Ludności z 1921 roku miejscowość zamieszkiwana była przez 174 osoby ( 79 mężczyzn i 95 kobiet ) w 33 domostwach .
Zdecydowana większość mieszkańców , w liczbie 122 osób , zadeklarowała wyznanie prawosławne , co stanowiło ponad 70 % wszystkich mieszkańców Guszczewiny , zaś pozostałe 52 osoby ( czyli niespełna 30 % ) zgłosiły wyznanie rzymskokatolickie .
Jednocześnie wszyscy mieszkańcy miejscowości podali polską przynależność narodową .
W owym czasie miejscowość nosiła nazwę `` Huszczewina '' i znajdowała się w gminie Masiewo powiatu Białowieża .
