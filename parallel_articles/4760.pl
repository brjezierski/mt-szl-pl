Gucin ( Warszawa )
Gucin – część dzielnicy Ursynów , położona częściowo na wzgórzu służewskim w pobliżu kościoła św .
Katarzyny , częściowo zaś u jego podnóża , pomiędzy ul .
Fosa i aleją Wilanowską .
Gucin założony został w latach 1817–1821 przez Stanisława Kostkę Potockiego jako subrezydencja wilanowska .
Nazwa pochodzi od wnuka Stanisława Kostki , Augusta ( Gucia ) Potockiego .
Tę romantyczną rezydencję Potocki utworzył wykorzystując istniejący zespół dworski , składający się z niewielkiego pałacyku , stawu i budynków gospodarczych .
Część z nich została przebudowana lub uzupełniona nowymi .
