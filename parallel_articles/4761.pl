Guźlin
Guźlin – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie włocławskim , w gminie Brześć Kujawski .
W latach 1975–1998 miejscowość administracyjnie należała do województwa włocławskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 402 mieszkańców .
Jest piątą co do wielkości miejscowością gminy Brześć Kujawski .
