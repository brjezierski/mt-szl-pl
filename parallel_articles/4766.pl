Gwatemala
Gwatemala ( `` República de Guatemala '' ) – państwo w Ameryce Środkowej , położone nad Oceanem Atlantyckim i Oceanem Spokojnym .
Graniczy z Salwadorem ( 203 km ) , Hondurasem ( 256 km ) , Meksykiem ( 962 km ) , Belize ( 266 km ) – łączna długość granic wynosi 1687 km , ponadto 400 km wybrzeża morskiego .
Społeczeństwo .
Gwatemalczycy to stosunkowo młodzi ludzie ( 40 % ludności ma poniżej lat 14 ) .
Potomkowie Europejczyków oraz Latynosi stanowią tu tylko 59,4 % , reszta mieszkańców to w większości Majowie .
Większość mieszkańców to katolicy , protestanci i inni chrześcijanie .
Spora grupa ludzi wyznaje wierzenia Majów .
Struktura etniczna : Metysi i biali stanowią 59,4 % populacji , Kicze 9,1 % , Kaqchikel 8,4 % , Mam 7,9 % , Kekchi 6,3 % , pozostali Majowie 8,6 % , Indianie pochodzenia niemajowskiego 0,2 % a pozostali 0,1 % .
Religia .
Demografia religijna kraju zmieniła się znacząco w ciągu ostatnich kilku dekad , pod wpływem nacisków politycznych i społecznych .
Historycznie kraj był w przeważającej mierze rzymskokatolicki .
Jednak częściowo dzięki działaniom misyjnym rozkwitły różne grupy , szczególnie protestancko–ewangelikalne .
W Gwatemali nie ma oficjalnego spisu przynależności religijnej .
Struktura religijna kraju w 2015 roku według `` The Association of Religion Data Archives '' ( The ARDA ) :
Istnieje także niewielka populacja osób wyznania mojżeszowego , buddyzmu i muzułmanów .
Pewna część rdzennych katolików i protestantów łączy swoje wierzenia z praktykowaniem tradycyjnych rytuałów plemiennych .
Geografia .
Obszar Gwatemali dzieli się na cztery części : nizinę Petén , położoną w północnej części kraju , wybrzeże Morza Karaibskiego , łańcuchy górskie środkowej części kraju oraz wybrzeże Pacyfiku .
Przy spotykanym podziale na 3 krainy nizinę Petén łączy się z wybrzeżem karaibskim .
Łańcuchy górskie mają układ równoleżnikowy .
Łańcuch północny to ( od wschodu ) Alto Cuchumatanes , Sierra de Chamá i Sierra de Las Minas .
Dalej na południu ciągnie się Sierra Madre ( od Meksyku , poprzez Gwatemalę , do Hondurasu ) .
Dolina rzeki Motagua oddziela łańcuch Sierra Madre od pasma Los Altos .
Tu właśnie znajduje się najwyższy szczyt Gwatemali – wygasły lub drzemiący wulkan Tajumulco ( 4220 m n.p.m .
) .
Na północ od obszarów górskich znajduje się rozległa nizina Petén , zajmująca blisko 1/3 powierzchni kraju .
Jest ona bardzo rzadko zaludniona i pokryta gęstym lasem tropikalnym .
Wybrzeże karaibskie to obszar nizinny .
Leży tutaj największe w kraju jezioro – Izabal , płyną tędy większe rzeki Gwatemali : Motagua ( 400 km ) , Polochic ( 240 km ) , Sarstún .
Położona na południu nizina La Costa ciągnie się od granicy z Meksykiem do granicy z Salwadorem .
Jej szerokość nie przekracza 60 km .
Historia .
W czasach prekolumbijskich teren zamieszkiwany przez Indian związanych z kulturą Majów .
W 1523 tereny dzisiejszej Gwatemali zostały podbite przez hiszpańskich konkwistadorów , którzy założyli tam kolonię wchodzącą w skład Nowej Hiszpanii .
Od XVI wieku wybrzeże kraju atakowane było przez piratów angielskich a w XVIII wieku Brytyjczycy podjęli się nieudanej próby zajęcia wybrzeża .
