Gwinea Bissau
Gwinea Bissau ( port .
`` Guiné-Bissau '' ) , Republika Gwinei Bissau ( port .
`` República da Guiné-Bissau '' , wym .
[ ] ) – państwo położone w zachodniej Afryce , nad Oceanem Atlantyckim .
Graniczy z Senegalem od północy i Gwineą od wschodu .
Do Gwinei Bissau należą również liczne przybrzeżne wyspy , z których największe skupione są w Archipelagu Bijagós .
Państwo jest członkiem Unii Afrykańskiej i ECOWAS .
Geografia .
Kraj nizinny .
Najwyższy punkt sięga 300 m n.p.m .
Obszary przybrzeżne o charakterze bagiennym , wnętrze kraju zajmuje sawanna .
U wybrzeży położona jest grupa wysp Bijagos .
Historia .
Od XIII wieku do XV wieku terytorium dzisiejszego państwa znajdowało się w obrębie Imperium Mali , w XV i XVI wieku w Songhaj .
Tereny te zamieszkiwane były przez różnorakie grupy etniczne , w tym Fulan i Mandinka .
Od połowy XV wieku miała miejsce penetracja tych obszarów przez Portugalię .
W 1687 roku kolonizatorzy utworzyli ufortyfikowaną placówkę handlową , nazwaną Bissau .
Do XIX wieku Gwinea Bissau stanowiła miejsce handlu niewolnikami ( po jego zakazie proceder był kontynuowany nielegalnie ) .
Od 1879 roku pod nazwą Gwinea Portugalska .
W 1886 roku w wyniku porozumienia pomiędzy Francją a Portugalią znaczna część terytorium Gwinei Portugalskiej przypadła Francuzom .
