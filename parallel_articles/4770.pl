Gwiazda ( gimnastyka )
Gwiazda , przerzut bokiem − efektowne ćwiczenie gimnastyczne , polegające na pełnym obrocie wyprostowanego tułowia wokół osi podłużnej w płaszczyźnie czołowej .
Wykonanie ćwiczenia
Z postawy przodem do kierunku ruchu należy unieść ramiona w przód w górę z jednoczesnym wznosem jednej nogi w przód .
Następnie następuje wykonanie kroku w przód z jednoczesnym opadem tułowia oraz zamachem wyprostowaną nogą w tył .
Ręce opierają się o podłoże bokiem do kierunku wykonywania ćwiczenia .
Następuje dynamiczne przejście przez fazę stania na rękach , nogi znajdują się w pozycji rozkrocznej .
Tułów pozostaje wyprostowany .
Ręce kolejno odpychają się od podłoża i następuje przejście do stania w rozkroku z ramionami uniesionymi w górę na zewnątrz .
Cały element należy wykonać dynamicznie , w równym tempie .
Ćwiczenie to można wykonać z miejsca i z rozbiegu .
