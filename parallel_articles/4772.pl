`` Gwiezdne wojny '' osiągnęły komercyjny sukces , zarabiając do końca 2019 roku prawie 10 mld USD .
Aktualnie `` Gwiezdne wojny '' zajmują 2. miejsce w rankingu najbardziej dochodowych serii filmowych .
Seria stworzyła rozległą franczyzę – `` Star Wars Expanded Universe '' – rozszerzoną o powieści , komiksy , seriale telewizyjne oraz gry komputerowe .
W 2015 roku całkowita wartość franczyzy wyniosła 42 mld USD , co pozwoliło zająć jej 2. miejsce wśród najbardziej dochodowych franczyz .
Świat przedstawiony .
Zdanie `` Dawno , dawno temu w odległej galaktyce ... '' rozpoczynające każdy z filmów jest jedyną wskazówką co do miejsca i czasu akcji .
