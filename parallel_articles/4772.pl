Gwiezdne wojny ( ang .
`` Star Wars '' ) – amerykańska franczyza filmowa z gatunku space opera oparta na trylogii filmów stworzonej przez George ’ a Lucasa .
Franczyza została zapoczątkowana w 1977 roku premierą filmu `` Star Wars '' ( któremu później dodano podtytuł : ' ) .
Stał się on światowym popkulturowym fenomenem .
W kolejnych latach LucasFilm wyprodukował dwa sequele – ' ( 1980 ) oraz ' ( 1983 ) .
Wszystkie trzy filmy stanowią oryginalną trylogię `` Gwiezdnych wojen '' .
W latach 1999–2005 powstała trylogia prequeli – ' ( 1999 ) , ' ( 2002 ) i ' ( 2005 ) .
W 2012 roku firma Walt Disney Company przejęła LucasFilm za 4,06 mld USD i uzyskała prawa do dystrybucji kolejnych filmów serii .
Były dystrybutor 20th Century Fox zachował prawa do fizycznej dystrybucji dwóch pierwszych trylogii , jest właścicielem praw do filmu z 1977 roku , a także będzie posiadał prawa do części I , II , III , V i VI do maja 2020 roku .
Walt Disney Studios jest właścicielem praw do cyfrowej dystrybucji filmów , z wyjątkiem `` Nowej nadziei '' .
