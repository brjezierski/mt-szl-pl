3 lutego 1943 ) i Bruce ’ a Weigerta Paltrowa ( ur .
26 listopada 1943 , zm .
3 października 2002 ) , reżysera i producenta telewizyjnego .
Dorastała z młodszym bratem Jakiem ( ur .
26 września 1975 ) .
