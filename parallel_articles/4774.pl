Gwyneth Paltrow
Gwyneth Kate Paltrow ( ur .
27 września 1972 w Los Angeles ) – amerykańska aktorka , reżyserka i scenarzystka filmowa .
W 2010 otrzymała własną gwiazdę w Alei Gwiazd w Los Angeles znajdującą się przy 6931 Hollywood Boulevard .
