Protezy stomatologiczne
Protezy stomatologiczne – są to protezy stosowane w dentystyce w celu uzupełnienia lub odbudowy brakującego uzębienia .
Są wykonywane z metali lub ich stopów ( np .
z glinu , srebra , kobaltu , niklu , tytanu , rtęci ) , tworzyw sztucznych i ceramiki .
