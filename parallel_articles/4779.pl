Łysak piękny
Łysak piękny ( `` Gymnopilus bellulus '' ( Peck ) Murrill – gatunek grzybów należący do rodziny podziemniczkowatych ( `` Hymenogastraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gymnopilus , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1873 roku Charles Horton Peck nadając mu nazwę `` Agaricus bellulus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1965 roku Charles Horton Murrill .
