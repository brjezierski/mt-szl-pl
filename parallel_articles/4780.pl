Łysak wspaniały
Łysak wspaniały ( `` Gymnopilus junonius '' ( Fr . )
P.D .
Orton ) – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1821 r. Elias Fries nadając mu nazwę `` Agaricus junonius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1960 r. Peter Darbishire Orton .
Synonimy :
Władysław Wojewoda z współautorami w 1999 r. zaproponowali nazwę łysak wspaniały , dawniej w polskim piśmiennictwie opisywany był jako łuszczak wspaniały , łuskwiak wspaniały i bedłka wspaniała .
Morfologia .
Średnica 6–15 cm .
