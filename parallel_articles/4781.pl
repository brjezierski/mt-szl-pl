Łysak szerokoblaszkowy ( `` Gymnopilus liquiritiae '' ( Pers . )
P .
Karst . )
– gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gymnopilus , Strophariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Persoon nadając mu nazwę `` Agaricus liquiritiae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1879 r. P. Karst ..
Niektóre synonimy :
Nazwę polską podał Władysław Wojewoda w 1999 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako skórzak słodkogorzki i płomiennica szerokoblaszkowa .
