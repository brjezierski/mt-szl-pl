Łysak drobnołuskowy
Łysak drobnołuskowy ( `` Gymnopilus sapineus '' ( Fr . )
Murrill – gatunek grzybów należący do rodziny podziemniczkowatych ( `` Hymenogastraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gymnopilus , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1821 roku Elias Fries nadając mu nazwę `` Agaricus sapineus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1912 roku Charles Horton Murrill .
