Łysostopek wodnisty
Łysostopek wodnisty ( `` Gymnopus aquosus '' ( Bull . )
Antonín & amp ; Noordel . )
– gatunek grzybów należący do rodziny `` Omphalotaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gymnopus , Omphalotaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1781 r. Jean Baptiste Bulliard nadając mu nazwę `` Agaricus aquosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1997 r. Vladimir Antonín i Machiel Evert Noordeloos , przenosząc go do rodzaju `` Gymnopus '' .
Synonimy naukowe :
Nazwę polską podał Władysław Wojewoda w 2003 r. , dawniej gatunek ten w polskim piśmiennictwie mykologicznym opisywany był przez Franciszka Błońskiego jako bedłka wodnista .
Morfologia .
O średnicy 2,5–6,5 cm , gładki i nagi .
