Gymnosporangium confusum
Gymnosporangium confusum Plowr .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożyt niektórych gatunków roślin z rodziny różowatych : głogu , irgi , jarząbu , nieszpułki , pigwy i pigwowca .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gymnosporangium , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Niektóre synonimy :
Rozwój .
Jest to pasożyt dwudomowy , czyli taki , który dla pełnego cyklu rozwojowego potrzebuje dwóch gatunków roślin żywicielskich : część rozwoju odbywa na niektórych gatunkach roślin z rodziny różowatych , a część na różnych gatunkach jałowców ( zarówno na jałowcu sabińskim , jak i jałowcach z grupy `` Oxycedra '' ) .
Na roślinach z rodziny różowatych tworzą się ecja i pyknidia , na jałowcach telia .
Jest to więc rdza niepełnocyklowa .
Zimuje w postaci grzybni na pędach jałowca .
Wiosną na jałowcu z grzybni rozwijają się telia wytwarzające teliospory .
Wiosną kiełkują tworząc podstawki .
Na podstawkach wytwarzane są drogą płciową bazydiospory , dokonujące infekcji na liściach roślin .
