Nagoć sawinowa
Nagoć sawinowa ( `` Gymnosporangium sabinae '' ( Dicks . )
G. Winter ) – gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Wywołuje chorobę zwaną rdzą gruszy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gymnosporangium , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1785 r. James Dickson , nadając mu nazwę `` Tremella sabinae '' .
Do rodzaju `` Gymnosporangium '' przeniósł go w 1805 r. Candolle .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1884 r. Heinrich Georg Winter .
Synonimy :
Rozwój .
Jest to pasożyt dwudomowy , czyli taki , który dla pełnego cyklu rozwojowego potrzebuje dwóch gatunków roślin żywicielskich : część rozwoju odbywa na jałowcu sabińskim , a część na gruszach .
