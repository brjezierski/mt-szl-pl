Gierałcice ( powiat kluczborski )
Gierałcice ( niem .
`` Jeroltschütz '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie kluczborskim , w gminie Wołczyn .
Od 1950 r. miejscowość należy administracyjnie do województwa opolskiego .
