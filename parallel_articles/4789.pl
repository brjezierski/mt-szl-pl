Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Gyromitra , Discinaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1973 r. Josef Breitenbach i Rudolph Arnold Maas Geesteranus , nadając mu nazwę `` Discina parma '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1974 r. František Kotlaba i Zdeněk Pouzar , przenosząc go do rodzaju `` Gyromitra '' .
