Gyöngyhajú lány
Gyöngyhajú lány – ballada rockowa zespołu Omega , wydana w 1970 roku na singlu `` Petróleumlámpa/Gyöngyhajú lány '' , promującym album `` 10000 lépés '' .
Powstanie i treść .
Po stworzeniu większości materiału na płytę `` 10000 lépés '' zespół Omega doszedł do wniosku , że brak na albumie wolniejszej , nastrojowej piosenki .
Istnieją rozbieżności dotyczące genezy melodii .
János Kóbor stwierdził , że jako melodię wybrano motyw , który Omega grała pod koniec lat 60. na zakończenie koncertów do słów „ Aratjuk a gabonát , szeretjük az Omegát ” .
Menedżer zespołu András Trunkos wspominał jednakże , że melodia , włączając melodię refrenu , powstała w wyniku improwizacji muzyków .
Trunkos dodał także , że pierwotnie tytuł piosenki brzmiał „ Egyszer majd visszajövünk ” , a autorem określenia „ gyöngyhajú lány ” ( `` dziewczyna o perłowych włosach '' ) był Gábor Presser .
Następnie zespół skontaktował się z Anną Adamis w celu dokończenia tekstu .
Adamis w tamtym okresie szeroko współpracowała z Omegą , wcześniej współtworząc popularny na Węgrzech album `` Trombitás Frédi és a rettenetes emberek '' .
Tekst jest tajemniczy i metaforyczny .
Traktuje o perlistowłosej dziewczynie , która zlitowała się nad ludźmi i niczym słońce oświetliła ciemność , w której żyli , a po świcie powróciła do domu .
Jednakże podmiot liryczny zapewnia , że w wielkiej samotności czeka na ludzi gwiazda , którą jest przemieniona dziewczyna znajdująca się pomiędzy niebem i ziemią .
Według Gyöngyia Bodolaia z „ Népújság ” wielu słuchaczy źle zrozumiało piosenkę , twierdząc , że wyraża ona nostalgiczne pożądanie czy tęsknotę za minioną miłością .
Wydanie .
W 1970 roku piosenka została wydana jako strona B na singlu `` Petróleumlámpa/Gyöngyhajú lány '' .
Umieszczenie piosenki na stronie B wynikało z przekonania Qualitonu , iż „ Petróleumlámpa ” jako piosenka skoczna odniesie większy sukces .
W 1977 roku nastąpiło wznowienie singla .
W 1970 roku wydano wersję anglojęzyczną piosenki pt .
„ Pearls in Her Hair ” .
W roku 1973 ukazała się wersja niemieckojęzyczna , zatytułowana „ Perlen im Haar ” .
Odbiór .
Piosenka została nagrodzona w 1970 roku na festiwalu Barbarella w Palma de Mallorca oraz na World Popular Song Festival w Tokio .
Utwór znalazł się także we wszystkich zestawieniach Top wszech czasów , najwyżej będąc notowanym na szesnastym miejscu .
W 1995 roku utwór zajął piąte miejsce na liście przebojów Programu Trzeciego .
W roku 2018 piosenka była notowana na 28. pozycji węgierskiego zestawienia Single Top 40 .
