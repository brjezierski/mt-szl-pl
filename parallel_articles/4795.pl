Gyöngyös
Gyöngyös – miasto węgierskie ( 32,64 tys .
mieszkańców w I 2011 r. ) położone u stóp góry Sárhegy , na zachód od gór Mátra .
Położone jest w sąsiedztwie autostrady M3 , między Egerem a stolicą Węgier , 80 km na północny wschód od Budapesztu .
Gyöngyös jest centrum położonego na wulkanicznych glebach regionu winiarskiego Mátraalja ( Podnóże Mátry ) oraz bramą do gór Mátra .
Miasto prowadzi wymianę partnerską z powiatem kolskim .
