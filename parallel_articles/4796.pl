Stokrotka ( film )
Stokrotka ( fr .
`` En Effeuillant la marguerite '' ) – francuski czarno-biały film komediowy z 1956 roku .
Fabuła .
Główna bohaterka , Agnès , jest młodą dziewczyną , która buntuje się przeciwko wszelkim konwenansom .
Jej ojciec , konserwatywny , emerytowany generał nie akceptuje zachowania córki .
Pewnego dnia Agnès publikuje pod pseudonimem skandalizującą książkę .
Obawiając się kary ojca , ucieka z domu i zostaje striptizerką .
