Gävle
Gävle – miasto w Szwecji nad Zatoką Botnicką .
Siedziba władz administracyjnych gminy Gävle w regionie Gävleborg .
Około 68 700 mieszkańców .
Prawa miejskie uzyskało w 1419 r i jest najstarszym miastem Norrland .
Gävle jest starym miastem przemysłowym , które dziś jest czasem kojarzone z fabryką o nazwie marki kawy `` Gevalia '' , która jest łacińską nazwą miasta .
