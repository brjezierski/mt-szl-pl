Gómez Palacio
Gómez Palacio – miasto w zachodniej części Meksyku w stanie Durango , nad rzeką Nazas .
Według spisu mieszkańców z 2005 r. miasto liczyło 240 tys .
Miejscowość została założona przez Don Juan Ignacio Jiménez z przekształcenia hacjendy de Santa Rosa de Lima w dniu 30 sierpnia 1840 roku .
Nazwa miasta pochodzi od nazwiska Francisco Gómez Palacio y Bravo pisarza , dziennikarza i gubernatora stanu Durango w latach 60 .
XIX wieku .
Leży w ważnym regionie rolniczym .
W mieście rozwinął się przemysł spożywczy , włókienniczy , odzieżowy , chemiczny oraz hutniczy .
Gmina Gómez Palacio .
Miasto Gómez Palacio jest siedzibą władz drugiej co do liczebności gminy w stanie Durango .
Liczba ludności w 2005 roku wynosiła 304 515 .
Obszar gminy ( 1082,76 km² ) wraz z 14 innymi gminami stanu Durango oraz 5 ze stanu Coahuila tworzą obszar metropolitarny nazywany Torreon lub Comarca Lagunera .
Pierwsze osadnictwo na danym terenie rozpoczęło się wraz utworzeniem hacjendy Francisco Fray Pedro Espinaredy w 1567 , a następnie z założeniem dwóch misji ; `` de Santiago Mapimí '' oraz w 1589 `` Santa María de las Parras '' w 1598 .
Gmina została utworzona w 1905 roku przez wydzielenie z części terytorium gminy Lerdo .
Wtedy to też nadano nazwę na cześć Francisco Gómez Palacio y Bravo .
