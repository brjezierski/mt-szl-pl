Gölcük
Gölcük − miasto w Turcji w prowincji Kocaeli .
W mieście znajduje się baza tureckiej marynarki wojennej a także zakład motoryzacyjny Ford Otosan .
17 sierpnia 1999 roku miasto nawiedziło trzęsienie ziemi o magnitudzie 7,6 w skali Richtera powodując śmierć niemal 10 tys .
mieszkańców miasta .
Według danych na rok 2000 miasto zamieszkiwało 55 790 osób .
