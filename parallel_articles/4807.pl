Getynga
Getynga ( , dolnoniem .
`` Chöttingen '' ) – akademickie miasto powiatowe w Niemczech , nad rzeką Leine , na południowym krańcu kraju związkowego Dolna Saksonia , siedziba powiatu Getynga .
W roku 2013 miasto liczyło 116 891 mieszkańców .
Jeden z głównych ośrodków naukowych kraju ( ponad 30 tys .
studentów ) .
Liczące się centrum turystyczne oraz kulturalne .
Symbolem miasta jest niewielka fontanna – figura Gęsiareczki z 1901 r .
