Güstrow ( stacja kolejowa )
Güstrow – stacja kolejowa w Güstrow , w kraju związkowym Meklemburgia-Pomorze Przednie , w Niemczech .
Znajdują się tu 3 perony .
Połączenia .
Güstrow jest stacją końcową dla linii S2 oraz S3 S-Bahn w Rostocku .
Ponadto kursują składy Regional-Express w kierunku Lubeki , Szczecina oraz Elsterwerdy .
