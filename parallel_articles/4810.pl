Għaxaq
Għaxaq ( malt .
`` Ħal Għaxaq '' , wym .
/ħal a'ʃaʔ/ ) – jedna z jednostek administracyjnych na Malcie .
Mieszka tutaj 4 722 osób .
Sport .
W miejscowości funkcjonuje klub piłkarski Ghaxaq F.C .
Powstał w 1950 roku .
Obecnie gra w `` Maltese Second Division '' , trzeciej w hierarchii ligowej .
Handel .
W miejscowości znajduje się supermarket Lidl oraz szereg mniejszych sklepów .
