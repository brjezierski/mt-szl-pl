Szyp
Szyp ( `` Acipenser nudiventris '' ) – gatunek ryby z rodziny jesiotrowatych ( Acipenseridae ) .
Występowanie .
Występuje w Morzu Czarnym , Morzu Azowskim oraz południowej części Morza Kaspijskiego i w Jeziorze Aralskim .
W Dunaju tworzy niewędrowną formę słodkowodną , rozmnażającą się w Wagu , Cisie i Maruszy .
Pozostałe populacje to ryby anadromiczne .
Cechy morfologiczne .
Osiąga długość 1–1,5 m ( maksymalnie 2 m ) i masę około 10 kg ( maksymalnie 80 kg ) .
Ciało jest grube , krótkie i krępe .
Cechą odróżniającą od innych jesiotrów jest warga dolna , która u szypa nie jest przerwana .
Grzbiet ma barwę ciemnobrązową lub szarą , okrywa go 12–15 wygiętych do tyłu tarczek kostnych .
Boki są jaśniejsze , a brzuch prawie biały .
Pysk krótki , wąsiki okrągłe w przekroju , wyposażone w cienkie naprzydatki na wewnętrznej stronie .
Płetwa grzbietowa jest stosunkowo długa , położona przy końcu ciała .
Odżywianie .
Zjada głównie bezkręgowce ( przede wszystkim mięczaki ) i czasem ryby .
Rozród .
Do rzek wchodzi jesienią , zimę spędza w ich najgłębszych partiach .
Tarło odbywa się od lutego do maja , w wyższych partiach głębokich , wartkich rzek , na żwirowatym dnie .
Znaczenie gospodarcze .
Mięso szypa jest wykorzystywane do wędzenia i produkcji konserw .
Największe znaczenie ma w Jeziorze Aralskim .
W Morzach Czarnym i Azowskim jest bardzo rzadki i nie ma większego znaczenia gospodarczego .
