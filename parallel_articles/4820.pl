Głuszec ( Białoruś )
Głuszec ( , `` Hłuszec '' ; , `` Głuszec '' ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie prużańskim , w sielsowiecie Suchopol .
W latach 1921–1939 należała do gminy Suchopol .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 43 osoby , wśród których 39 było wyznania prawosławnego a 4 rzymskokatolickiego .
Jednocześnie wszyscy 8 mieszkańców zadeklarowało polską przynależność narodową a 35 białoruską .
We wsi było 13 budynków mieszkalnych .
