Głębinów
Głębinów ( ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Nysa .
Łącznie ze Skorochowem tworzy wspólne sołectwo gminy Nysa .
Skorochów zalicza się także do jednostki spisowej Głębinów w GUS , w której spis powszechny z 2011 roku odnotował 394 mieszkańców .
Wieś wypoczynkowa leży nad Jeziorem Nyskim , nad którym jest wiele ośrodków wypoczynkowych .
