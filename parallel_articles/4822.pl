Głębocko ( jezioro )
Jezioro Głębocko – jezioro w województwie wielkopolskim , w gminie Murowana Goślina , w zachodniej części Głębocka , na terenie Parku Krajobrazowego Puszcza Zielonka .
Akwen typu rynnowego ma 3,55 ha powierzchni .
Przepływa przez niego Trojanka .
Jest udostępnione do wędkowania z brzegu i łodzi .
Przy zachodnim brzegu przebiega droga gminna z Łopuchowa do Boduszewa .
