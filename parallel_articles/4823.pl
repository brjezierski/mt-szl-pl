Głódki
Głódki – wieś w Polsce położona w województwie mazowieckim , w powiecie makowskim , w gminie Szelków .
Wieś szlachecka `` Głodki '' położona była w drugiej połowie XVI wieku w powiecie makowskim ziemi różańskiej .
W latach 1975–1998 miejscowość administracyjnie należała do województwa ostrołęckiego .
