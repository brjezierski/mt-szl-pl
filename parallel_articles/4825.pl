Gniezno ( stacja kolejowa )
Gniezno – stacja kolejowa w Gnieźnie w województwie wielkopolskim , w Polsce na 50. km linii kolejowej liczącej 186,5 km z Poznania do Bydgoszczy przez Toruń .
Jedna z największych i najbardziej rozbudowanych stacji w województwie wraz z kompleksem parowozowni .
Jedna z czterech stacji kolejowych w mieście Gniezno : Gniezno , Gniezno Winiary oraz Gniezno Wąskotorowe i jedyna czynna dla ruchu pasażerskiego .
