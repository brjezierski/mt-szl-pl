Historia .
Historia stacji kolejowej w Gnieźnie rozpoczyna się 30 listopada 1867 roku , kiedy podpisano umowę na budowę kolei z Poznania przez Gniezno i Inowrocław do Torunia z odgałęźnieniem do Bydgoszczy .
Pierwsze prace ziemne wykonano we wrześniu 1868 roku w rejonie obecnej stacji kolejowej w Trzemesznie .
