Góra Gunnbjørna
Góra Gunnbjørna ( duń .
`` Gunnbjørns Fjeld '' ) – najwyższy szczyt Grenlandii , a zarazem Arktyki .
Znajduje się w środkowo-wschodniej części wyspy , w odległości ok. 150 km od wschodniego wybrzeża .
Jego wysokość wynosi 3694 m n.p.m .
Nazwa szczytu pochodzi od imienia norweskiego żeglarza Gunnbjörna Ulfssona .
Góra Gunnbjørna oraz szczyty Cone i Dome tworzą najwyższe pasmo Gór Watkinsa i mają postać nunataku : szczytu wznoszącego się nad powierzchnię lodowca .
Ekspedycje .
Po raz pierwszy szczyt został zdobyty 16 sierpnia 1935 roku przez ekspedycję brytyjsko-duńską , w skład której wchodzili m.in .
Augustine Cortauld i Laurence Wager .
W maju 2004 roku na szczycie stanęła Marzena Kaczmarska , uczestniczka wyprawy kierowanej przez Espena Nordahla i członek Międzynarodowego Towarzystwa Glacjologicznego .
Ekspedycja ta przebadała dokładnie teren i znajdujących się w paśmie Gór Watkinsa .
6 czerwca 2008 szczyt zdobyła pierwsza polska wyprawa ( `` „ HiFlyer Polar Ice Expedition 2008 ” '' ) na Gunnbjørn Fjeld złożona z Leszka Cichego – kierownika , Marka Kamińskiego , Tomasza Walkiewicza , Mirosława Polowca i Ryszarda Rusinka .
