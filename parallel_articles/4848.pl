Górecko ( Bytom )
Górecko ( pniem .
`` Guretzko '' , `` Guretzky '' ) – historyczny obszar Bytomia , położony w obecnych granicach dzielnicy Rozbark , między Rozbarkiem a piekarskim Szarlejem , w okolicy ulicy Kędzierzyńskiej .
Górecko to dawny folwark rozbarski na Górnym Śląsku .
Do 1898 roku Górecko było samodzielną jednostką administracyjną .
Według spisu z 1874 roku była to gmina jednostkowa ( `` Landgemeinde '' ) w powiecie bytomskim , liczącą 3 grudnia 1867 roku 185 mieszkańców , a 1 grudnia 1871 roku 164 mieszkańców .
Według spisu z 1885 spisane już jest jako obszar dworski ( `` Gutsbezirk '' ) , liczący 292 mieszkańców .
1 października 1898 jednostkę zniesiono , włączając ją do gminy Rozbark .
Odtąd dwór górecki pełnił funkcję rozbarskiego ratusza gminnego ( obecnie w tym miejscu znajdują się dwa wieżowce ) .
W związku ze zniesieniem gminy Rozbark 1 stycznia 1927 , obszar Górecka został włączony w granice Bytomia .
