Grzędy Górne
Grzędy Górne ( niem .
`` Oberkonradswaldau '' ) – wieś w Polsce , położona w województwie dolnośląskim , w powiecie wałbrzyskim , w gminie Czarny Bór , u podnóża Pasma Lesistej ( Góry Kamienne ) oraz Krzeszowskich Wzgórz w Sudetach Środkowych .
Podział administracyjny .
W latach 1975–1998 miejscowość należała administracyjnie do województwa wałbrzyskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyły 164 mieszkańców .
Są najmniejszą miejscowością gminy Czarny Bór .
Nazwa .
1 czerwca 1948 nadano miejscowości , będącej wówczas administracyjnie związanej z Grzędami , polską nazwę Grzędy Górne .
