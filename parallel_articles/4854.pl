Rekowo Górne
Rekowo Górne ( kaszb .
`` Rekòwò '' ) – osada kaszubska w Polsce położona w województwie pomorskim , w powiecie puckim , w gminie Puck na wschodnim krańcu Puszczy Darżlubskiej w pobliżu drogi wojewódzkiej nr .
216 .
`` Rekowo Dolne '' do niedawna także sołectwo gminy Puck zostało włączone w obszar miejski miasta Redy jako jej północna dzielnica .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Od 1970 roku w Rekowie mieści się Radiokomunikacyjne Centrum Odbiorcze Gdynia/Rekowo .
Do końca 2003 obiekt był wykorzystywany do łączności morskiej i wraz ze stacją nadawczą w Gdyni Oksywiu wchodził w skład stacji brzegowej `` Gdynia-Radio '' .
Po zamknięciu stacji większość masztów zdemontowano , pozostawiono jeden , na którym swoją antenę nadawczą miało Radio Kaszëbë .
Maszt zawalił się 2 stycznia 2015 r. w wyniku silnych podmuchów wiatru .
W miejscowości znajdują się również dawne budynki Warzywniczego Zakładu Doświadczalnego , które są w trakcie renowacji .
