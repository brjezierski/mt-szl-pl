Łaziska Górne ( przystanek kolejowy )
Łaziska Górne – przystanek kolejowy w Łaziskach Górnych , w województwie śląskim , w Polsce .
Aż do lat 50 .
XX wieku przystanek znajdował się na terenie miejscowości Mokre , obecnie będącej dzielnicą Mikołowa .
Na przełomie XIX i XX wieku powstał niewielki dworzec kolejowy , rozebrany w latach 80 .
XX wieku .
Wybudowano wówczas nowy budynek dworca ( w 1986 ) , służący pasażerom tylko kilkanaście lat , do końca XX wieku .
Następnie obiekt został zamknięty przez PKP i , pozbawiony opieki , stopniowo niszczał .
Ostateczna rozbiórka nastąpiła pod koniec 2005 .
W roku 2017 przystanek obsługiwał 150–199 pasażerów na dobę .
