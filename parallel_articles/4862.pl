Dłużyna Górna
Dłużyna Górna – wieś w Polsce położona w województwie dolnośląskim , w powiecie zgorzeleckim , w gminie Pieńsk .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa jeleniogórskiego .
Nazwa miejscowości .
Po II wojnie światowej wieś nosiła początkowo kilka nazw : `` Długojów , Długojewo , Dłużyce '' .
Historia .
Dłużyna Górna leży we wschodniej części Górnych Łużyc , choć w pierwszej połowie XIV w. i od 1815 związana ściślej z Dolnym Śląskiem .
Założona ( wraz z Dłużyną Dolną jako jedna wieś ) przypuszczalnie w pierwszych dziesięcioleciach XIII w. w wyniku kolonizacji Górnych Łużyc podjętej przez królów czeskich .
Przez wiele stuleci była jedną z największych wsi puszczańskich rady miejskiej Zgorzelca .
Od 1319 wraz z pobliskim Pieńskiem wieś stanowiła część piastowskiego księstwa jaworskiego , jednego z polskich księstw dzielnicowych na Dolnym Śląsku , po czym jeszcze w XIV wieku przypadła Koronie Czeskiej , w której granicach leżała do 1635 .
