Górne Łużyce
Górne Łużyce ( , g.-łuż .
`` Hornja Łužica '' , d.-łuż .
`` Górna Łužyca '' , , ) – kraina historyczno-geograficzna w Niemczech ( kraj związkowy Saksonia , częściowo Brandenburgia ) , część tego obszaru leżąca na zachód od rzeki Kwisy znajduje się na terytorium Polski ( województwo dolnośląskie , kilka miejscowości w województwie lubuskim ) .
Głównym miastem jest Budziszyn .
Do początku XV wieku tereny te znane były jako Milsko , od zamieszkującego je plemienia słowiańskiego Milczan .
Na tych ziemiach wykształcił się język górnołużycki .
