Bukówiec Górny
Bukówiec Górny – wieś w Polsce , położona w województwie wielkopolskim , w powiecie leszczyńskim , w gminie Włoszakowice , w południowo-zachodniej Wielkopolsce z doskonale zachowanym , wciąż żywym folklorem .
W latach 1954-1959 wieś należała i była siedzibą władz gromady Bukowiec Górny .
W latach 1975–1998 wieś administracyjnie należała do województwa leszczyńskiego .
Krótka historia .
Pierwsza wzmianka o wsi pochodzi z 1210 roku , wiadome jest jednak , że osada istniała tu już wiele lat wcześniej .
Historię wsi spisaną przez Stanisława Malepszaka wydano w obszernej monografii `` Bukówiec Górny na tle dziejów krainy przemęckiej '' ( Leszno 1993 ) .
Zachowane od końca XVI wieku księgi parafialne wskazują na wielowiekowe tradycje chłopskich rodów .
Bukówiec Górny znany był z patriotyzmu .
