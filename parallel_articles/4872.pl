Mokronos Górny ( przystanek kolejowy )
Mokronos Górny – przystanek kolejowy w Mokronosie Górnym w województwie dolnośląskim .
We wrześniu 2018 r. podpisano umowę na budowę przystanku w formie dwóch jednokrawędziowych peronów zlokalizowanych przy al .
Konwaliowej w Mokronosie Górnym .
Planowany termin oddania inwestycji do użytku określono na 2019 r. , a jej koszt na 3,5 mln zł .
Jako oficjalną datę uruchomienia przystanku wskazano 15 grudnia 2019 r .
