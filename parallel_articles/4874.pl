Za górami , za lasami
Za górami , za lasami ( rzadziej `` Hej , Górale ! '' )
– śląska pieśń ludowa .
Pochodzi z obszaru .
Jest znana w Czechach , Polsce i na Słowacji .
Pod koniec XX wieku została wylansowana przez czeską grupę muzyczną Čechomor , która umieściła ją w swoim albumie `` Gorale '' .
Obecnie pieśń jest śpiewana przez kilka muzycznych grup .
Tekst .
Tekst piosenki składa się z motywu opowiadającego o sporze górali o dziewczynę oraz refrenu śpiewanego przez chór , który stara się pogodzić górali , przekonując , że góralka ma zawsze dwie rzeczy takie same .
Jest to metafora , polegająca na tym , że dziewczyn na świecie jest zawsze wystarczająco dużo .
Do pieśni ludowej ustalono polską popularną literacką wersję utworu :
`` Za górami ''
`` za lasami ''
`` za dolinami ''
`` pobili się dwaj górale ciupagami ''
`` Hej górale ! ''
`` Nie bijcie się ! ''
`` Ma góralka dwa warkocze ( duże serce , dwoje oczu , chyże nogi , swego chłopca ) ''
`` podzielicie się ! ''
Inne .
Tę samą melodię posiada jedna z interpretacji Sto lat ( sto lat sto lat , sto lat sto lat niechaj żyje nam ) .
