Szerokość pasma waha się od 8 do 20 km .
Karkonosze zajmują powierzchnię ok. 650 km² , z czego do Polski należy 185 km² czyli 28,46 % .
Głównym grzbietem oraz Grzbietem Lasockim przebiega granica polsko-czeska .
