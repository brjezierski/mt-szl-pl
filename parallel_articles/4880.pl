Gółkowo
Gółkowo – wieś w Polsce położona w województwie wielkopolskim , w powiecie słupeckim , w gminie Słupca .
Wieś duchowna `` Gołkowo '' , własność biskupstwa poznańskiego , pod koniec XVI wieku leżała w powiecie pyzdrskim województwa kaliskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa konińskiego .
