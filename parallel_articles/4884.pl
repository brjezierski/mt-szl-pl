Haarlem
Haarlem ( IPA : [ ˈɦaːr.lɛm ] ) – miasto w płn.-zach .
Holandii na zachód od Amsterdamu , położone nad rzeką Spaarne .
Jego historia sięga ponad 750 lat .
Dawna stolica hrabstwa Holandii ;
Stolica prowincji Noord-Holland ( `` Holandia Północna '' ) .
Liczba mieszkańców ok. 154 tys .
( 2012 ) .
Haarlem słynie z wielu interesujących muzeów , a także wspaniałych budowli , jak chociażby Katedra św .
Bawona , ratusz ( XVII w. ) , czy też Vleeshal ( `` targ mięsny '' ) .
Liczne biblioteki , muzea ( m.in .
dzieł Fransa Halsa , czy najstarsze w Holandii Teylers Museum ) , siedziba Noordhollands Philharmonisch Orkest , galerie sztuki ; zespół zabytkowy skupiony wokół Grote Markt : kościoły , Dom Wagi Miejskiej ( 1598 ) , hale rzeźnicze ( 1602-1603 ) , liczne kamienice mieszczańskie ( XVI-XVIII w. ) .
Ośrodek handlu cebulkami tulipanów .
Przemysł włókienniczy , maszynowy , chemiczny .
Muzeum Fransa Halsa ( 1581/85-1666 ) ( `` Frans Halsmuseum '' ) , który działał i zmarł tutaj .
Siedziba diecezji rzymskokatolickiej od 1853 .
Święty Bawo jest patronem miasta ( według legendy w 1274 roku zstąpiwszy z nieba obronił Haarlem przed najeźdźcami ) , znajdują się tu dwa kościoły pod jego wezwaniem , była katedra protestancka - umieszczony na rynku Kościół św .
Bawona ( St. Bavokerk , zwany także z uwagi na swoje rozmiary Grote Kerk , XIV-XVI ) oraz obecna katedra katolicka - Katedra św .
Bawona ( Kathedraal St. Bavo , XIX w. ) , co powoduje często dezorientację wśród turystów .
W mieście znajduje się stacja kolejowa Haarlem .
