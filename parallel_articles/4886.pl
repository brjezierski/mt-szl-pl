Hachan ( Karaimi )
Hachan – duchowy i świecki zwierzchnik społeczności karaimskiej , sprawował pieczę nad zbiorem dżymatów ( gmin karaimskich ) .
Nazwa wywodzi się od chazarskiego słowa `` kagan '' ( wielki chan ) .
Jego pozycja przypominała pozycję kalifa wśród muzułmanów , łączącego dożywotnio przywództwo świeckie i religijne .
Nie był on wybierany przez duchowieństwo , ale przez przedstawicieli społeczności karaimskich zwanych morianami .
Pełniona przez niego funkcja różniła się od lokalnego przywódcy religii karaimskiej zwanego `` hacham '' ( hebr .
mędrzec ) .
Turkijską nazwę `` hachan/chagan '' zaczęto przywoływać na początku XX w ..
Według praw kodyfikujących strukturę wyznaniową XIX-wiecznej Rosji , duchowieństwo karaimskie składało się z hachanów , hazzanów i szamaszów .
