Hacienda Heights
Hacienda Heights – miejscowość spisowa ( obszar niemunicypalny ) w Stanach Zjednoczonych , w stanie Kalifornia , w hrabstwie Los Angeles .
Wchodzi w skład obszaru metropolitalnego Los Angeles .
Z Hacienda Heights pochodzi Fergie , amerykańska piosenkarka R & amp ; B i pop oraz autorka tekstów muzycznych .
