Hadam Bohuchwał Šěrach
Hadam Bohuchwał Šěrach , Adam Gottlob Schirach ( ur .
5 listopada 1724 w Nostitz , zm .
3 kwietnia 1773 w Kleinbautzen ) – łużycki duchowny protestancki i pszczelarz o międzynarodowej renomie .
Życiorys .
Proboszcz we wsi Kleinbautzen ( Budziszynek ) .
Członek towarzystw naukowych , m.in .
Cesarskiej Akademii Nauk Przyrodniczych i Rosyjskiego Towarzystwa Ekonomicznego w Petersburgu .
Caryca Katarzyna II wysłała dwóch badaczy na naukę do księdza Šěracha .
Proboszcz z Budziszynka był przeciwnikiem pietystów i polemizował z nimi .
Šěrach był najwybitniejszym przedstawicielem Oświecenia wśród Serbów Łużyckich w 2. połowie XVIII w .
