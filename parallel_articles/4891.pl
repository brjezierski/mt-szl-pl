Hadera
Hadera ( hebr .
חדרה , `` Chadira '' ; arab .
الخضيرة , `` Al-Chudajra '' ) – miasto położone w dystrykcie Hajfa w Izraelu .
Położenie .
Leży na równinie Szaron na wybrzeżu Morza Śródziemnego , w otoczeniu miasteczek Eljachin i Pardes Channa-Karkur , moszawów Michmoret , Achituw , Sede Jicchak , Ma ’ or i Talme Elazar , kibuców Giwat Chajjim , Gan Szemu ’ el i Sedot Jam , oraz wioski Cezarea .
Leży mniej więcej w połowie odległości pomiędzy dwoma ważnymi miastami Tel Awiwem a Hajfą .
Historia .
Badania archeologiczne odkryły tutaj wielką manufakturę szkła , pochodzącą z VII wieku .
Jest ona pierwszą tak wielką manufakturą odnalezioną na świecie .
W odkrytej manufakturze znajduje się około dwudziestu urządzeń do produkcji szklanych kulek ( zwanych „ bullo ” ) , w kolorach turkusu , zieleni oliwkowej i brązu .
Wyrabiano z nich rozmaite szklane przedmioty .
Współczesne miasto zostało założone w 1891 roku przez żydowskich imigrantów z Rosji i Europy Wschodniej , członków syjonistycznego ruchu Chowewej Syjon .
Ziemia została kupiona przez syjonistycznego aktywistę Jahoszuę Hankina , od chrześcijańskiego efendi Selim Khuri .
Była to ziemia o niskiej jakości rolniczej , na której wcześniej zamieszkiwało jedynie kilka arabskich rodzin , trudniących się pasterstwem i sprzedażą trzciny .
