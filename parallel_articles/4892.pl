Haderslev
Haderslev ( niem .
`` Hadersleben '' ) – miasto w Danii , siedziba gminy Haderslev w regionie Dania Południowa ; ośrodek przemysłowy .
Historia .
II wojna światowa .
Miasteczko Haderslev obsadzały oddziały III batalionu ( 4 kompania uzbrojona w 10 lkm , 5 ciężka kompania z 4 ckm i 2 średnimi moździerzami ) , pułkowa kompania ppanc .
