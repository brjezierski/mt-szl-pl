Owies zwyczajny
Owies zwyczajny ( `` Avena sativa '' L. ) – gatunek zbóż należący do rodziny wiechlinowatych .
Uprawiany jest powszechnie w strefie umiarkowanej Eurazji oraz Ameryki Północnej .
Został udomowiony przez człowieka w czasach prehistorycznych , około 6000 lat temu .
Gatunek ten wykazuje niewielką zmienność genetyczną .
Jest heksaploidem ( 2n=6x=42 chromosomy )
Morfologia .
Roślina jednoroczna osiągająca do 110 cm wysokości , o pustym źdźble łodygi .
Kłos z rozpierzchłą lub chorągiewkowatą wiechą o długości 10–30 cm .
Ziarna oplewione .
Kwiatostanem owsa jest wiecha o długości 10–30 cm .
Osią wiechy jest przedłużenie ostatniego międzywęźla , czyli osadka , która jest podzielona węzłami i międzywęźlami .
Z węzłów tych wyrastają okółkowo rozgałęzienia boczne tworząc gałązki wiechy zakończone kłoskami – wiecha prosta .
Częściej jednak rozgałęzienia boczne są także poprzedzielane węzłami na międzywęźla , z których wyrastają rozgałęzienia I rzędu ( wiecha podwójna ) , a z nich rozgałęzienia II rzędu ( wiecha właściwa ) , zakończone pojedynczymi kłoskami .
Ze względu na kształt wiech wyróżnia się dwa typy odmian hodowlanych owsa : o wiesze chorągiewkowatej i rozpierzchłej .
W wiesze rozpierzchłej gałązki boczne są ustawione pod różnymi kątami , co warunkuje różny kształt wiechy ( rozstrzelona , krzaczasta , zwisła lub sztywna-wzniesiona ) .
W wiesze kłoski mogą być 2-3 kwiatowe , stąd zawierają 2-3 ziarniaki .
Stosunek mas ziarniaków ma się jak 3:2:1 stąd pierwszy ziarniak ( zewnętrzny ) ma masę największą , drugi mniejszą , a trzeci najmniejszą .
