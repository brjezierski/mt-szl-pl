Hafnarfjörður
Hafnarfjörður – miasto w południowo-zachodniej Islandii , położone u nasady półwyspu Reykjanes , po jego północnej stronie , nad zatoką Hafnarfjörður ( część Zatoki Faxa ) .
Wchodzi w skład zespołu miejskiego Wielkiego Reykjavíku , tworzy gminę Hafnarfjarðarkaupstaður położoną regionie stołecznym .
Od północy graniczy z miastem Garðabær .
Stanowi trzecią pod względem liczby ludności miejscowość Islandii po stolicy kraju Reykjavíku i po położonym nieco bardziej na północ Kópavogur .
Na początku 2018 roku zamieszkiwało je 29,4 tys .
