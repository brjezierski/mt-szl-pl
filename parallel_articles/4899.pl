Hajdarabad ( księstwo )
Hajdarabad – muzułmańskie księstwo istniejące w południowośrodkowych Indiach w latach 1724-1948 , ze stolicą w mieście Hajdarabad .
Władcy tego organizmu państwowego nosili tytuł nizama .
Państwo to przestało istnieć w 1948 roku w rezultacie operacji militarnej nowo powstałego państwa indyjskiego .
W roku 1956 jednostkę administracyjną Hajdarabad podzielono według kryteriów językowych ( obecnie stany Maharasztra , Karnataka i Andhra Pradesh ) .
