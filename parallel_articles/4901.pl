Hajfa ( dystrykt )
Hajfa ( hebr .
: מחוז חיפה , `` Mechoz Chefa '' ) – jeden z sześciu administracyjnych dystryktów w Izraelu .
Dystrykt ma powierzchnię 864 km² , zamieszkany jest przez 858 000 mieszkańców ( dane z 2007 ) , a jego stolicą jest Hajfa .
