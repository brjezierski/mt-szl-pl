Haiti ( wyspa )
Haiti ( , , ) – wyspa na Morzu Karaibskim , druga co do wielkości ( po Kubie ) wyspa Wielkich Antyli ( 76 192 km² ) .
Terytorium wyspy zajmują dwa państwa – Haiti na zachodzie i Dominikana na wschodzie .
Jest to najludniejsza wyspa Karaibów , w 2011 roku zamieszkana przez 20 180 000 osób .
Najwyższy szczyt – Pico Duarte 3098 m n.p.m. , długość linii brzegowej – 3059 km .
Toponimia .
Do XIX wieku w języku polskim używano nazwy Hispaniola .
Hispaniola to zlatynizowana wersja hiszpańskiej nazwy La Española , czyli „ ( Wyspa ) Hiszpańska ” , nadanej w 1492 podczas pierwszej wyprawy Kolumba .
Później używano także nazw Santo Domingo i San Domingo .
W wielu językach nadal używa się nazwy Hispaniola .
Historia .
Wyspa odkryta w 1492 przez Kolumba , proklamowana została kolonią hiszpańską .
Koloniści bezpośrednio lub pośrednio spowodowali wyginięcie rdzennej ludności – Indian z plemienia Tainów , a do prac na zakładanych plantacjach sprowadzili czarnoskórych niewolników z Afryki , w wyniku czego czarnoskórzy szybko stali się dominującą liczebnie grupą ludności .
Do 1667 cała wyspa znajdowała się pod panowaniem hiszpańskim .
W wyniku stopniowego podboju przez korsarzy francuskich w 1697 zachodnia część wyspy , jako Santo Domingo , znalazła się we władaniu Królestwa Francji .
W 1791 pod wpływem rewolucji francuskiej wybuchły bunty niewolników .
Po zniesieniu niewolnictwa powstańcy we francuskiej części wyspy proklamowali w 1801 niezależną republikę Haiti , a w 1803 pobili interwencyjne wojska Napoleona I Bonaparte złożone m.in .
