Henryk opolski
Henryk ( urodzony w 1337 lub 1338 roku - zmarł pomiędzy 1356 a 1365 rokiem ) , książę opolski pomiędzy 1356 a 1365 rokiem ( tylko formalnie - kariera duchowna ) .
Henryk był trzecim ( najmłodszym ) pod względem starszeństwa synem księcia Opola Bolka II i Elżbiety Świdnickiej .
O Henryku zachowało się niewiele informacji .
Jako najmłodszy z rodzeństwa został przeznaczony do kariery duchownej i jeszcze za życia ojca został klerykiem przy katedrze we Wrocławiu .
W 1356 roku rozpoczął starania o stanowisko kanonika katedry praskiej .
Nie wiadomo kiedy dokładnie zmarł ( na pewno w przedziale lat 1356-1365 , choć raczej bliżej drugiej z tych dat ) i gdzie został pochowany .
