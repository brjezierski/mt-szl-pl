Kilof
Kilof – narzędzie służące do rozdrabniania skał i wstępnego rozdrabniania twardego podłoża , które potem można usuwać na przykład za pomocą łopaty .
Kilofy wykorzystywane są w górnictwie i kamieniarstwie .
Kilof z jednym ostrzem nazywany jest oskardem .
Składa się z ostrza w kształcie wydłużonego i niekiedy zakrzywionego klina , osadzonego w końcowej części na drewnianym stylisku .
Niekiedy kilof posiada dwa ostrza po obu stronach styliska ( lub jedno ostrze spiczaste a drugie spłaszczone ) .
W górnictwie stosowano różne rodzaje kilofów : cięższe do bezpośredniego wydobywania miękkich i średnio twardych skał i lżejsze do wykonywania wrębów .
Niektóre kilofy posiadają wymienne ostrza .
Inne mają po przeciwnej stronie ostrza obuch , dzięki czemu można ich używać również jako młota , a jeszcze inne mają ostrze spłaszczone – zbliżone kształtem do motyki służące głównie do urabiania miękkich skał .
Pierwotną formą kilofa na ziemiach polskich był kopas wykonywany z jelenich rogów i stosowany przez starożytnych górników rudnych spod Łysej Góry .
Od niego też wziął swoją nazwę w XI wieku tamtejszy cech górniczy – Kopasynowie .
