Halasy
Halasy – wieś w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Międzyrzec Podlaski .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 566 mieszkańców i była czwartą co do wielkości miejscowością gminy Międzyrzec Podlaski .
