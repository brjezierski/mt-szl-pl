Halfords
Halfords Group plc – brytyjskie przedsiębiorstwo będące właścicielem sieci sklepów , oferujących m.in .
akcesoria i części samochodowe , systemy car-audio i nawigacji satelitarnej , rowery oraz akcesoria kempingowe i turystyczne , oraz stacji obsługi pojazdów ( Halfords Autocentre ) .
Siedziba przedsiębiorstwa mieści się w Redditch , w Anglii .
Przedsiębiorstwo zostało założone w 1892 roku przez F. W. Rushbrooke ' a , który otworzył w Birmingham sklep z narzędziami .
W 2011 Halfords było właścicielem ponad 460 sklepów na terenie Wielkiej Brytanii i Irlandii oraz około 250 stacji obsługi pojazdów , zatrudniając ponad 11 000 pracowników .
Spółka jest notowana na giełdzie London Stock Exchange ( symbol akcji : HFD ) .
