Halk Topluşu
Halk Topluşu ( wym .
`` halk topluszu '' ) to gagauski organ ustawodawczy , składający się z 35 deputowanych , wybieranych na czteroletnią kadencję w jednoznacznych okręgach terytorialnych w powszechnych , równych , tajnych i wolnych wyborach .
Członkiem Zgromadzenia Ludowego Gagauzji może być każdy obywatel Republiki Mołdawii , który ukończył 21 lat , posiada prawa wyborcze i zamieszkuje okręg wyborczy , który będzie reprezentował w wyborach .
