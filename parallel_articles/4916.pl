Hallein
Hallein – miasto powiatowe w Austrii , w kraju związkowym Salzburg , siedziba powiatu Hallein .
Leży nad rzeką Salzach , w Alpach Berchtesgadeńskich , przy granicy z Niemcami .
Liczy 21 200 mieszkańców ( 2019 ) .
W mieście rozwinął się przemysł papierniczy , kosmetyczny oraz silników elektrycznych .
