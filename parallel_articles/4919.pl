Halmstad
Halmstad – miasto ( `` tätort '' ) w Szwecji i port morski na jej zachodnim wybrzeżu .
Siedziba władz ( `` residensstad '' ) regionu administracyjnego ( `` län '' ) Halland .
Ośrodek administracyjny ( `` centralort '' ) gminy Halmstad .
Do 1970 roku Halmstad miał administracyjny status miasta .
W 2015 roku Halmstad liczył 66 124 mieszkańców .
Gospodarka .
W mieście rozwinął się przemysł maszynowy , metalowy , włókienniczy oraz hutniczy .
