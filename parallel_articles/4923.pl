Hamadan ( ostan )
Hamadan – ostan w zachodnim Iranie , w górach Zagros .
Stolicą jest Hamadan .
Geografia .
Ostan Hamadan położony jest w północno-zachodniej części Iranu , w obrębie czwartego regionu administracyjnego .
Od północy graniczy on z ostanami Zandżan i Kazwin , od wschodu z ostanem Markazi , od południa z Lorestanem , a od zachodu z Kermanszahem i Kurdystanem .
Zajmuje powierzchnię 19 367,1 km² .
Hamadan cechuje się położeniem wyżynnym .
W krajobrazie występują wysokie góry , zielone zbocza , pola uprawne , pastwiska , ośnieżone szczyty i żyzne doliny .
