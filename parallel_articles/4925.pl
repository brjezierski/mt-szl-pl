Hamamatsu
– miasto w Japonii , w prefekturze Shizuoka , w środkowej części wyspy Honsiu ( `` Honshū '' ) , nad Oceanem Spokojnym .
Największe pod względem liczby mieszkańców w tej prefekturze .
W lipcu 2005 miasto wchłonęło 11 otaczających je miast i miasteczek i wyprzedziło pod względem liczby ludności Shizuokę .
Historia .
W mieście znajduje się zamek zbudowany około 1532 roku .
Znany jest z tego , że w latach 1572–1588 mieszkał w nim założyciel dynastii siogunów Tokugawa , Ieyasu Tokugawa ( 1543–1616 ) , a więc w okresie przed bitwą pod Sekigaharą w 1600 i przeniesieniem stolicy z Kioto do Edo ( obecnie Tokio ) w 1603 roku .
Jak wiele zamków w Japonii , został zniszczony przez nalot lotniczy podczas II wojny światowej , ale został odbudowany w 1958 roku .
