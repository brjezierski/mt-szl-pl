Hanafuda
– rodzaj japońskich kart do gry , wykorzystywanych do różnych gier karcianych .
Obecnie używa się kart hanafuda także na Hawajach i w Korei .
W latach 70. i 80 .
XX wieku była popularna wśród japońskiej mafii yakuza .
Karty hanafuda wykorzystuje się do różnych gier .
Najbardziej znane to : koi-koi , hachi-hachi , oicho-kabu .
Od 1889 roku do 1964 roku Nintendo produkowało te karty .
Karty .
Talia składa się z 48 kart , podzielonych na 12 miesięcy .
Karty zdobią tradycyjne motywy roślinne i zwierzęce .
Każdy miesiąc jest symbolizowany przez inną roślinę .
Każdemu miesiącowi przyporządkowane są cztery różne karty z różnymi wartościami punktowymi .
Typowy miesiąc składa się z dwóch kart z motywem roślinnym ( 1 punkt ) , jednej karty ze zwojem ( 5 punktów ) i jednej karty z motywem zwierzęcym lub symbolicznym ( 10 lub 20 punktów ) .
Koi-koi .
Koi-koi najbardziej znana i najłatwiejsza gra kartami hanafuda .
Oto przykład dla dwóch graczy :
Na początek kładzie się osiem odkrytych kart na stół , każdy gracz dostaje osiem kart .
Pierwszy gracz wykłada kartę najlepiej z pasującym do którejś z leżących na stole kart miesiącem .
Wtedy może zabrać swoją kartę i kartę z pasującym miesiącem ze stołu .
Jeśli nie uda mu się dopasować karty to ta , którą wyłożył zostaje na stole .
Następnie gracz bierze kartę z kupki i próbuje ją dopasować .
Jeśli mu się uda , zabiera karty , jeśli nie , karta zostaje na stole .
Gdy na stole znajdują się trzy karty z danego miesiąca , gracz może zgarnąć je wszystkie , dopasowując czwartą kartę .
Kolejny gracz postępuje analogicznie .
Każdy gracz wykłada zdobyte karty przed sobą tak , aby wszyscy je widzieli ( odkryte ) .
Rozgrywka toczy się w kierunku przeciwnym do wskazówek zegara .
Celem gry jest zdobycie określonych układów kart .
Gdy jeden z graczy zdobędzie taki układ , może zakończyć całą partię lub grać dalej , próbując zdobyć więcej punktów .
Gdy po rozdaniu gracz posiada wszystkie cztery karty z danego miesiąca , wygrywa automatycznie .
Istnieje wiele wariantów regionalnych tej gry z różnym zestawem reguł .
