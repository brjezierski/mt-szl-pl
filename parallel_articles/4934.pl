Hanceville
Hanceville - miasto w południowo-wschodnich Stanach Zjednoczonych , w stanie Alabama , w hrabstwie Cullman .
Według spisu powszechnego z 2010 roku , miasto to liczyło 2982 mieszkańców .
Geografia .
Według danych amerykańskiego Biura Spisów Ludności ( U.S. Census Bureau ) , miasto zajmuje powierzchnię 10,908 km² , z czego 10,871 km² stanowią lądy , a 0,037 km² stanowią wody .
