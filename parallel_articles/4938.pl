Hanford ( Staffordshire )
Hanford – osada w Anglii , w Staffordshire , w dystrykcie ( unitary authority ) Stoke-on-Trent .
Leży 3,3 km od miasta Stoke-on-Trent , 19,8 km od miasta Stafford i 217,3 km od Londynu .
W latach 1870–1872 miejscowość liczyła 832 mieszkańców .
Hanford jest wspomniana w Domesday Book ( 1086 ) jako `` Heneford '' .
