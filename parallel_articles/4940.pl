Hanower
Hanower ( ) – miasto w Niemczech , stolica kraju związkowego Dolna Saksonia ( ) oraz związku komunalnego Region Hanower .
Leży nad rzeką Leine .
Siedziba Stowarzyszenia Krajowego ( `` Landesverband '' ) Technisches Hilfswerk obejmującego swym zasięgiem landy Dolna Saksonia oraz Brema .
Historia .
Średniowiecze .
Pierwsze wzmianki o osadzie targowej w miejscu dzisiejszego miasta pochodzą z 1022 r. W XII w. w mieście rezydował książę Henryk Lew z dynastii welfickiej .
Prawa miejskie Hanower otrzymał od cesarza w 1241 r .
W XIV wieku miasto zostało okalane murem miejskim , w skład którego weszły 34 baszty obronne .
Wiele z tych baszt zachowało się do dziś ( np .
Pferdeturm , Döhrener Turm , Lister Turm , Beginenturm ) .
W tym samym wieku zbudowano także bramy miejskie ( np .
Steintor ) i kilka gotyckich kościołów ( Marktkirche , Kreuzkirche ) .
W XIV w. był członkiem Hanzy .
Państwo hanowerskie i unia z Wielką Brytanią .
W czasach Reformacji , mieszczanie Hanoweru przychylali się do głoszonych przez ruch reformatorski haśle i w czasie zgromadzenia na miejskim rynku w czerwcu 1533 r. ogłosili swoje poparcie dla haseł głoszonych przez Marcina Lutra .
Bardziej konserwatywna Rada miejska próbowała powstrzymać zmiany , lecz wola mieszczan okazała silniejsza , co doprowadziło do ucieczki Rady do pobliskiego katolickiego miasta Hildesheim .
W 1636 r. swą siedzibę przeniósł do Hanoweru książę Calenbergu .
Odtąd miasto było stolicą Elektoratu Hanoweru , nieistniejącego już państwa niemieckiego .
Elektorat Hanoweru był od 1714 połączony unią personalną ( Jerzy I Hanowerski , zm .
1727 ) z Wielką Brytanią .
Bardzo zdolnym ministrem Jerzego I był Andreas Gottlieb von Bernstorff ( 1640–1726 ) , który podobnie jak kompozytor Georg Friedrich Haendel ( 1685–1750 ) pojechał z królem do Londynu .
W 1734 r. król i jego minister Gerlach Adolph von Münchhausen założyli uniwersytet w Getyndze ( tak zwana : `` Georgiana '' ) .
Szybko przybyli na niego cenieni wykładowcy , tacy jak Samuel Christian Hollmann .
W latach 1725–1767 burmistrzem miasta był Christian Ulrich Grupen ( 1692–1767 ) . . Był człowiekiem wykształconym , budował kościoły , odnawiał budynki użyteczności publicznej .
Patronował badaniom naukowym i historycznym .
Od 1814 Hanower był stolicą nowo utworzonego Królestwa Hanoweru .
W 1837 wygasła unia Wielkiej Brytanii i Hanoweru .
W 1866 miasto Hanower zostało wraz z całym państwem wcielone do Prus po ataku pruskich wojsk .
Odtąd do 1946 roku Hanower był stolicą Prowincji Hanower .
W granicach zjednoczonych Niemiec .
W 1871 roku prowincja i miasto znalazły się wraz z Królestwem Prus w granicach zjednoczonych Niemiec .
W 1900 roku Hanower był 10. największym miastem Niemiec .
W czasie II wojny światowej pierwszy aliancki samolot znalazł się nad Hanowerem już 4 września 1939 roku , jednak wówczas doszło jedynie do zrzutu ulotek propagandowych .
1 sierpnia 1940 doszło do rzeczywistego bombardowania centrum miasta .
Najbardziej nasilony okres bombardowań miał miejsce 9 października 1943 roku w tzw .
`` Czarny Dzień '' , kiedy to w wyniku nalotów zginęło ponad 1000 osób .
Ostatecznie , na koniec wojny , Hanower był silnie zniszczony wskutek bombardowań z centrum w 90 % leżącym w gruzach .
W 1945 znalazł się w brytyjskiej strefie okupacyjnej Niemiec .
23 sierpnia 1946 został stolicą nowo utworzonego kraju Hanower , który istniał 92 dni .
Z połączenia Hanoweru oraz wolnych krajów Brunszwiku , Oldenburga i Schaumburg-Lippe jeszcze w 1946 utworzono kraj związkowy Dolna Saksonia , w którym Hanower pełni funkcję stołeczną .
W 2014 roku Hanower był trzynastym pod względem wielkości miastem Niemiec .
W mieście zamieszkują liczne grupy obcokrajowców , najliczniejsze stanowią przybysze z Turcji , Polski i Grecji .
Podział administracyjny .
Miasto dzieli się na trzynaście dzielnic ( `` Stadtbezirk '' ) : Ahlem-Badenstedt-Davenstedt , Bothfeld-Vahrenheide , Buchholz-Kleefeld , Döhren-Wülfel , Herrenhausen-Stöcken , Kirchrode-Bemerode-Wülferode , Linden-Limmer , Misburg-Anderten , Mitte , Nord , Ricklingen , Südstadt-Bult oraz Vahrenwald-List .
Dzielnice te dzielą się na 51 części miasta ( `` Stadtteil '' ) .
Gospodarka .
Rozwinięty jest przemysł środków transportu ( samochody , lokomotywy , wagony ) , elektrotechniczny ( zakłady Telefunken ) , chemiczny , włókienniczy , gumowy Continental AG i spożywczy ( czekolada ) oraz usługi finansowe ( HDI , VHV , Hannover Rück , Mecklenburgische ) .
W mieście odbywają się międzynarodowe targi przemysłowe np .
Targi Hanowerskie ( `` Hannover Messe '' ) .
Od 1986 do 2018 co roku odbywały się tu największe targi IT na świecie zwane CeBIT .
W 2000 Hanower był gospodarzem Światowej Wystawy Expo 2000 .
Transport .
Na północ od miasta znajduje się port lotniczy Hanower ( HAJ ) .
W mieście funkcjonuje system kolei miejskiej – Stadtbahn Hannover .
Stacje kolejowe : Hannover Hauptbahnhof , Hannover-Nordstadt , Hannover Bismarckstraße , Hannover Messe/Laatzen , Hannover-Kleefeld , Hannover Karl-Wiechert-Allee , Hannover Anderten-Misburg .
