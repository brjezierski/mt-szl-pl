Hans Majestet Kongens Garde
Hans Majestet Kongens Garde ( HMKG ) ( `` Gwardia Jego Królewskiej Mości '' ) – elitarny batalion lekkiej piechoty z Oslo .
Batalion odgrywa podwójną rolę – jest norweską gwardią królewską strzegącą bezpieczeństwa rodziny monarszej , ochrania główną rezydencję króla – Pałac Królewski w Oslo ( `` Slottet '' ) , rezydencję następcy tronu – `` Skaugum '' ( na przedmieściach stolicy ) , Dwór Królewski na półwyspie Bygdøy oraz Twierdzę Akershus , jest również główną jednostką wojskową w Oslo .
Nominalnie głównodowodzącym batalionu jest panujący król Norwegii .
Od 1972 maskotką oraz patronem batalionu jest pingwin królewski Nils Olav ( obecnie Nils Olav III ) , żyjący w ZOO w Edynburgu .
Główną siedzibą gwardii królewskiej jest Huseby Leir położone w dawnym gospodarstwie będącym własnością norweskiego rządu od XVIII wieku .
W jednostce służy ok. 900 żołnierzy oraz 250–400 rekrutów .
