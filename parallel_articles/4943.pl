Hans Otte
Hans Günther Franz Otte ( ur .
3 grudnia 1926 w Plauen , zm .
25 grudnia 2007 w Bremie ) – niemiecki kompozytor i pianista .
Życiorys .
Studiował w Weimarze ( 1946–1947 ) , Stuttgarcie ( 1948–1950 ) i na Yale University ( 1950–1951 ) .
Jego nauczycielami byli Johann Nepomuk David i Paul Hindemith ( kompozycja ) , Walter Gieseking i Bronisław von Poźniak ( fortepian ) oraz Hermann Abendroth ( dyrygentura ) i Fernando Germani ( organy ) .
W 1959 roku przebywał na stypendium w Rzymie .
Przez wiele lat koncertował i nagrywał jako pianista .
Od 1959 do 1984 roku kierował redakcją muzyczną radia w Bremie .
Był propagatorem muzyki współczesnej .
W 1961 roku zorganizował w Bremie cieszący się międzynarodowym uznaniem festiwal Pro Musica Nova , gdzie wykonywano odrzucane na kursach darmsztadzkich utwory Johna Cage ’ a i La Monte Younga .
Twórczość .
Początkowo pozostawał pod wpływem Paula Hindemitha , później przeszedł na pozycje awangardowe , adaptując serializm i aleatoryzm .
W latach 60. w poszukiwaniu nowych środków wyrazu zaczął komponować utwory multimedialne , z eksperymentalnymi tekstami , dźwiękiem i instalacjami świetlnymi .
W latach 80. zainteresował się medytacją zen , w konsekwencji zbliżając się do nurtu new age .
