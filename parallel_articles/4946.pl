Miękusz rabarbarowy
Miękusz rabarbarowy ( `` Hapalopilus nidulans '' ( Fr . )
P .
Karst . )
– gatunek grzybów z rzędu żagwiowców ( `` Polyporales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hapalopilus , Phanerochaetaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten w 1821 r. Elias Magnus Fries zdiagnozował jako `` Polyporus nidulans '' , później przez różnych badaczy był włączany do rodzajów `` Boletus '' ( borowik ) , `` Fomes '' ( hubiak ) , `` Inonotus '' ( błyskoporek ) , `` Poria '' , `` Polystictus '' , `` Scindalma '' , `` Phaeolus '' ( murszak ) .
Obecnie za prawidłową uznaje się diagnozę P. Karsta , który w 1881 r. zaliczył go do rodzaju `` Hapalopilus '' .
Ma ok. 20 synonimów naukowych .
Nazwę polską podał Stanisław Domański w 1965 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako huba korkowa i huba czerwonawa .
