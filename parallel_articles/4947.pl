Haplomyces texanus
Haplomyces texanus ( Thaxt . )
Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Corethromyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1893 r. na pokrywach skrzydeł owada `` Beldius rubiginosus '' .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski w 1994 i 2003 r. opisał jego występowanie na chrząszczach z rodziny kusakowatych ( Staphylinidae ) : `` Bledius atricapillus '' , `` Bledius gallicus '' , `` Bledius longulus '' .
