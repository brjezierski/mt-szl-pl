Harlow
Harlow – miasto i dystrykt w Wielkiej Brytanii , w Anglii , w hrabstwie Essex , na północny wschód od Londynu .
W 2011 roku dystrykt liczył 81 944 mieszkańców .
W mieście rozwinął się przemysł poligraficzny , precyzyjny oraz meblarski .
Konflikty etniczne w XXI wieku .
W sierpniu 2016 roku w Harlow doszło do głośnego zabójstwa Polaka .
