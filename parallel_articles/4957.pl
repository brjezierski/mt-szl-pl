Harold Pinter
Harold Pinter ( ur .
10 października 1930 w Londynie , zm .
24 grudnia 2008 tamże ) – angielski dramaturg , pisarz , reżyser teatralny i scenarzysta , laureat literackiej Nagrody Nobla w 2005 , otrzymanej za odkrywanie „ przepaści pod codzienną gadaniną i wymuszanie wejścia do zamkniętych przestrzeni ucisku ” ( uzasadnienie ) .
Zaliczany do czołowych przedstawicieli teatru absurdu .
Pinter był też twórcą tzw .
„ komedii zagrożenia ” ( `` comedy of menace '' ) , obnażającej pozorność komunikacji między ludźmi i dramatyczną bezradność człowieka zarówno wobec świata zewnętrznego , jak i własnej podświadomości ( `` Urodziny Stanleya '' , 1958 , przekład pol .
„ Dialog ” 10/1960 ) .
Dzieciństwo .
Pinter urodził się w żydowsko-brytyjskiej rodzinie , w dzielnicy Hackney w Londynie .
Uczył się w Hackney Downs Grammar School i krótko w Royal Academy of Dramatic Art ( RADA ) .
Jako młodzieniec publikował poezję .
Jak podawał sam dramaturg , jego żydowscy dziadkowie przybyli z Polski i Odessy .
Kariera .
Harold Pinter rozpoczął pracę w teatrze jako aktor , pod pseudonimem „ David Baron ” .
Jego pierwsza sztuka teatralna , `` The Room '' , została wykonana przez studentów Uniwersytetu w Bristolu w 1957 .
Druga sztuka Pintera ( która jest obecnie jedną z najbardziej znanych tego autora ) , `` The Birthday Party '' ( 1958 ) , nie odniosła sukcesu , pomimo pozytywnej recenzji w „ The Sunday Times ” , napisanej przez wybitnego krytyka Harolda Hobsona .
Jednakże po sukcesie `` The Caretaker '' w 1960 , `` The Birthday Party '' została ponownie przedstawiana w teatrach i była pozytywnie odbierana .
W 1973 został współpracownikiem dyrektora Royal National Theatre .
Jego późniejsze sztuki były krótsze , wielokrotnie uznawane jako alegorie ucisku .
Harold Pinter był dwukrotnie nominowany do Nagrody Akademii Filmowej ( Oscar ) w kategorii scenariusza adaptowanego ( za `` Kochanicę Francuza '' na podstawie powieści Johna Fowlesa – 1981 oraz `` Betrayal '' – 1983 ) .
W styczniu 2005 Harold Pinter ogłosił , iż odstępuje od pisania sztuk teatralnych , ponieważ postanowił zaangażować się w działalność polityczną .
Działalność polityczna .
Pinter postulował swobodę wypowiedzi podczas wieloletniej współpracy z PEN Clubem .
W 1985 dołączył do misji amerykańskiego dramaturga Arthura Millera International PEN-Helsinki Watch Committee do Turcji , mającej na celu zbadanie i protest przeciwko torturom uwięzionych pisarzy .
Spotkał tam wiele osób będących ofiarami ucisku politycznego .
W ambasadzie amerykańskiej , w trakcie uroczystości , podczas której uhonorowano Millera , zamiast wymieniać uprzejmości , Pinter opowiadał o więźniach , którym podłączano prąd do genitaliów , i został za to wyrzucony ( Miller opuścił ambasadę wraz z Pinterem ) .
Doświadczenia z pobytu w Turcji oraz zakaz używania języka Kurdów zainspirowały go do napisania w 1988 sztuki `` Mountain Language '' .
Pinter sprzeciwiał się amerykańskiej inwazji na Afganistan i Irak .
Był także przeciwnikiem interwencji NATO w Kosowie .
Publicznie nazwał prezydenta Stanów Zjednoczonych , George ' a W. Busha , masowym mordercą , a premiera Wielkiej Brytanii , Tony'ego Blaira , „ a deluded idiot ” ( co można przetłumaczyć jako „ zbałamucony idiota ” ) .
Pinter wielokrotnie pisał listy o tematyce politycznej do brytyjskich gazet .
Porównywał administrację Busha do nazistowskich Niemiec Adolfa Hitlera .
Harold Pinter należał również do Międzynarodowego Komitetu Obrony Slobodana Miloševicia .
Pinter był prezesem Gaieties Cricket Club .
Był także honorowym członkiem Narodowego Towarzystwa Świeckiego ( `` National Secular Society '' ) .
