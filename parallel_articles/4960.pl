Harrogate
Harrogate – miasto w Wielkiej Brytanii , w północnej Anglii , w hrabstwie North Yorkshire , w dystrykcie Harrogate , u podnóża Gór Pennińskich ; znane uzdrowisko ( od 1596 ) , 88 źródeł mineralnych ( wody żelaziste , siarkowe ) ; przemysł spożywczy .
Liczy 84 000 mieszkańców .
W kwietniu 1982 odbył się tu finał 27 Konkursu Piosenki Eurowizji .
5 lipca 2014 w Harrogate znajdowała się meta inauguracyjnego etapu 101. edycji wyścigu kolarskiego Tour de France .
W 2019 miasto było gospodarzem Mistrzostw Świata w Kolarstwie Szosowym .
Z Harrogate pochodzi Livvi Franc , brytyjska piosenkarka .
W mieście rozwinął się przemysł skórzany i odzieżowy .
