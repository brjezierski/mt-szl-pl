Harry Kane
Harry Edward Kane ( ur .
28 lipca 1993 w Londynie ) – angielski piłkarz , występujący na pozycji napastnika w angielskim klubie Tottenham Hotspur oraz w reprezentacji Anglii , której jest kapitanem .
Kariera klubowa .
Kane zadebiutował w pierwszej drużynie Tottenhamu Hotspur 25 sierpnia 2011 w meczu Ligi Europy UEFA ze szkockim Heart of Midlothian .
Zanim na stałe wywalczył miejsce w podstawowej jedenastce „ Spurs ” , Kane spędził trzy lata na wypożyczeniach w Leyton Orient , Millwall , Norwich City i Leicester City .
Napastnik został kluczowym zawodnikiem Tottenhamu w sezonie 2014/2015 , w którym zdobył 21 bramek w Premier League .
Za swoją dobrą formę został wybrany „ Najlepszym Młodym Zawodnikiem Roku ( PFA Young Player of the Year ) ” .
W swoim kolejnym sezonie Kane strzelił 25 bramek i został królem strzelców Premier League .
Rok później napastnik pobił swój rekord strzelecki i zdobywając 29 goli ponownie został najlepszym strzelcem ligi angielskiej , a jego zespół zdobył wicemistrzostwo kraju .
5 lutego 2023 w meczu 22. kolejki Premier League został najlepszym strzelcem w historii Tottenhamu , wyprzedzając Jimmy'ego Greavesa , zdobywając 267. gola dla Tottenhamu i 200 w Premier League , wygrywając 1:0 u siebie z Manchesterem City .
Kariera reprezentacyjna .
27 marca 2015 zadebiutował w reprezentacji Anglii w eliminacjach do Mistrzostw Europy w meczu z Litwą .
Napastnik zaledwie dwie minuty po wejściu na boisko , wpisał się na listę strzelców .
Podczas Mistrzostw Świata 2018 zdobył 6 bramek i został królem strzelców .
