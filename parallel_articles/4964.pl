Harry S. Morgan
Harry S. Morgan , właściwie Michael Schey ( ur .
29 sierpnia 1945 w Essen , zm .
30 kwietnia 2011 w Düsseldorfie ) – niemiecki producent , reżyser i aktor filmów pornograficznych , dziennikarz .
Życiorys .
Wczesne lata .
Urodził się w Essen , gdzie w 1966 ukończył Neusprachlichen Gymnasium .
Po odbyciu zasadniczej służby wojskowej , pracował przez dwa lata jako praktykant Jensa Feddersena w redakcji gazety regionalnej „ Neue Ruhr Zeitung ” w Essen .
W tym samym czasie uczęszczał do szkoły dziennikarskiej w Monachium .
Następnie pracował trzy lata dla dziennika „ Bild ” w Zagłębiu Ruhry .
W następnych latach pracował jako freelancer dla tygodnika „ Der Spiegel ” , „ Stern ” i „ Bild am Sonntag ” .
Studiował fotografię na akademii artystycznej Folkwang-Hochschule w Essen pod kierunkiem profesora Otto Steinerta i był współzałożycielem grupy „ Visum ” .
Morgan był właścicielem agencji public relations w Düsseldorfie .
W ramach raportu magazynu „ Der Spiegel ” na temat pornografii zetknął się po raz pierwszy z przemysłem pornograficznym w 1972 .
Zaowocowało to pierwszymi zleceniami o pornografii dla jego agencji .
W połowie lat 70. pracował tymczasowo jako fotograf przy filmach porno w okolicach Monachium .
Później porzucił agencję , by pracować jako asystent operatora w Bavaria Film w Monachium .
Pracował między innymi jako asystent kamery u Josta Vacano .
Jego ostatnią produkcją w Bawarii w tej funkcji był czteroczęściowy film historyczny ZDF `` Wallenstein '' ( 1978 ) .
W latach 80. pracował jako niezależny reporter aktualności dla RTL .
Kariera .
W 1986 jako reżyser debiutował filmem pornograficznym `` Happy Video Privat '' , gdzie przeprowadzał wywiady z „ parami z sąsiedztwa ” uprawiającymi potem seks w ich domu .
Był narratorem `` Radio Bizarr '' ( 1988 ) .
Współpracował z francuskim reżyserem Gabrielem Pontello na planie produkcji `` Heiss am Stiel '' ( 1989 ) i `` Seksowne kociaki '' ( `` Teeny Exzesse 8 – Sexy Cats '' , 1989 ) .
W 1991 wyreżyserował film detektywistyczny `` Pommes Rot-Weiß '' z udziałem Danuty Lato .
Związał się m.in .
z wytwórnią Videorama .
Zrealizował ponad 400 produkcji hardcore , w tym cykle filmów pornograficznych `` Teeny Exzesse '' i `` Maximum Perversum '' .
W jego produkcjach wzięli udział m.in .
: Dolly Buster , Vivian Schmitt , Horst Baron , Christoph Clark , Steve Holmes , David Perry , Mick Blue i Rocco Siffredi ( `` Deep Inside Carol Lynn '' , 1993 ) .
30 kwietnia 2011 nie pojawił się na planie filmowym .
Zaniepokojeni współpracownicy otworzyli jego mieszkanie w Düsseldorfie , gdzie znaleźli ciało reżysera .
Rzecznik miejscowej policji poinformował , że na miejscu nie znaleziono żadnych śladów morderstwa .
Miał 65 lat .
