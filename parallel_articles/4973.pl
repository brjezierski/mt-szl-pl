Twaróg pokruszony i posolony pozostawiany jest w ciepłym miejscu , aż do uzyskania charakterystycznego ostrego zapachu i ciągnącej konsystencji .
Czasem dla przyśpieszenia procesu gliwienia dodawana jest soda oczyszczona .
Następnie jest on w zależności od lokalnego zwyczaju , gotowany , często na łaźni wodnej , lub smażony na maśle .
Jako tradycyjna przyprawa stosowany jest kminek , ale dodawane bywają też majeranek , pieprz , papryka , cząber .
Pod koniec obróbki termicznej dodawane są żółtka jaj i całość po dokładnym wymieszaniu wlewana jest do foremek jako gotowy już produkt .
Niektóre przepisy zalecają używanie całych jajek .
W zależności od wilgotności twarogu , ilości użytego masła i żółtek ser może mieć po wystygnięciu konsystencję pomazankową lub półtwardą .
Charakteryzuje się ostrym , charakterystycznym zapachem , smakiem i żółtawą barwą .
Niektórzy smakosze cenią szczególnie ten ser przed wystygnięciem , spożywając go w sposób podobny do fondue .
