Havelberg
Havelberg , Hansestadt Havelberg ( słowiański `` Hobolin '' lub `` Hawolin '' ) – miasto w środkowych Niemczech , w kraju związkowym Saksonia-Anhalt , w powiecie Stendal , nad rzeką Hawelą .
Miasto połączone z Łabą kanałem żeglownym .
Historia .
W 948 król Otton I Wielki , po ustanowieniu niemieckiej kontroli nad w większości słowiańskimi mieszkańcami okolicznych ziem , ustanowił w Havelbergu biskupstwo , co uznano za prowadzenie pracy misyjnej wśród Słowian .
Jednak po śmierci Ottona I ( 973 ) i klęsce Ottona II w bitwie pod Krotoną , wybuchło w 983 powstanie Słowian , w trakcie którego miasto uległo zniszczeniu .
