Hawaje ( archipelag )
Hawaje ( ; , dawniej : Sandwich Islands , nazwane tak przez Cooka na cześć Johna Montagu , hrabiego Sandwich ) – archipelag pochodzenia wulkanicznego w Polinezji na Oceanie Spokojnym .
Niemal całe Hawaje są jednym z amerykańskich stanów ( liczącym 1,2 mln mieszkańców , o powierzchni ok. 28,3 tys .
km² ) .
Jedynie atol Midway , leżący w północno-zachodniej części archipelagu , nie należy do stanu Hawaje .
Historia .
Królestwo Hawajów .
Hawaje zostały zaludnione w IV wieku przez Polinezyjczyków z Tahiti , którzy dotarli tutaj po mniej więcej 4 wiekach wypraw podejmowanych śladami lecących w tym kierunku ptaków .
Dla Europy odkryte zostały przez Jamesa Cooka w 1778 .
Początkowo Polinezyjczycy potraktowali Cooka jak bóstwo , które przybyło do nich na ogromnym statku .
Kiedy jednak Cook opuścił wyspę i musiał na nią powrócić po parodniowym sztormie , uznali , że jest to świadectwo tego , iż nie jest on bogiem .
Podczas zbrojnego starcia zginął wtedy Cook i kilku jego marynarzy .
Rządy rodów arystokratycznych pojawiły się na Hawajach na długo przed przybyciem Europejczyków , jednakże istnienie skonsolidowanego królestwa datuje się od 1796 roku , kiedy władzę na Hawajach objął król Kamehameha I. W czasach tych w kulturze europejskiej wyspy hawajskie funkcjonowały pod dwiema nazwami : Wyspy Sandwich ( nazwa nadana archipelagowi w 1778 roku przez Jamesa Cooka ) i Hawaje ( nazwa lokalna , od największej wyspy , Hawaiʻi ) .
W roku 1810 po zjednoczeniu się wysp Oʻahu , Maui , Molokaʻi , Lānaʻi i Hawaiʻi powstało królestwo .
W 1840 roku przyjęto na wyspach pierwszą konstytucję wzorowaną na prawach brytyjskich i amerykańskich .
Gwarantami suwerenności państwa były Stany Zjednoczone , Francja i Wielka Brytania .
W latach 1852 , 1864 i 1887 uchwalano kolejne konstytucje .
Od 1840 królestwo posiadało parlament .
Administracyjnie państwo dzieliło się na 4 prowincje : Kauaʻi , Oʻahu , Maui i Hawaiʻi .
W 1851 roku król Kamehameha III powierzył pieczę nad swym królestwem Stanom Zjednoczonym .
W 1887 roku USA uzyskało prawo do wybudowania portu w zatoce Pearl Harbor .
W 1893 roku miejscowa ludność amerykańska obaliła rządy królowej Liliʻuokalani i ustanowiła rządy republikańskie .
Republika Hawajów .
Republika Hawajów powstała w latach 1894–1898 .
