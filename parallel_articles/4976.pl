Hawana
Hawana ( lub skrócone `` La Habana '' ) – miasto nad Zatoką Meksykańską , stolica Kuby i największe miasto oraz port na wyspie i w całych Karaibach .
Główny ośrodek przemysłowy kraju , gdzie obecny jest przemysł tytoniowy , petrochemiczny , włókienniczy , elektromaszynowy .
Węzeł komunikacji lądowej , morskiej i lotniczej ( międzynarodowe lotnisko im .
José Martí ) .
Posiada Uniwersytet od 1728 .
Historia .
Miasto założył około 1515 hiszpański konkwistador Diego Velázquez de Cuéllar na południowym wybrzeżu wyspy .
W 1519 Hawana została przeniesiona na obecne miejsce ( stąd według niektórych źródeł została założona w 1519 ) .
Początkowo była tylko portem handlowym , ale w 1607 została stolicą hiszpańskiej kolonii : Kuby .
W 1728 powstał w Hawanie Uniwersytet .
Mimo wielu napadów angielskich , francuskich i holenderskich piratów miasto dynamicznie się rozwijało i szybko stało się głównym hiszpańskim portem w Nowym Świecie .
Z wyłączeniem okupacji angielskiej w 1762 podczas wojny siedmioletniej do 1898 należała do Hiszpanii .
Zatonięcie 15 lutego 1898 w hawańskim porcie pancernika USS Maine ( prawdopodobnie w wyniku wybuchu kotła ) stało się pretekstem do wybuchu wojny amerykańsko-hiszpańskiej , w wyniku której Hawana wraz z całą wyspą przeszła pod amerykańską okupację .
W 1902 Hawana została stolicą Republiki Kuby .
W pierwszej połowie XX wieku stała się ważnym ośrodkiem turystycznym .
8 stycznia 1959 została zajęta przez rewolucyjne oddziały Fidela Castro .
Prowadzona przez Castro polityka gospodarcza doprowadziła do zmierzchu świetności miasta , ale począwszy od lat 80 .
XX wieku prowadzone są zabiegi renowacyjne w obrębie starego miasta .
W lipcu 2015 uruchomiono pierwszą w kraju strefę dostępu do internetu , z której mogą korzystać Kubańczycy .
Wcześniej internet dostępny był jedynie w strefach zamieszkania zagranicznych turystów .
Hawana była ulubionym miejscem Ernesta Hemingwaya , który spędził tu 20 lat swojego życia .
Zabytki .
W Hawanie znajdują się liczne zabytki architektury kolonialnej .
Z tego powodu stare miasto La Habana Vieja zostało w 1982 wpisane na listę światowego dziedzictwa UNESCO .
Na starym mieście znajduje się Plaza de la Catedral , czyli główny plac starego miasta z barokową katedrą .
