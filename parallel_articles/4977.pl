Płatki owsiane
Płatki owsiane – płatki produkowane z owsa zwyczajnego .
Zawierają ok. 68 % węglowodanów , 13 % białka , 7 % tłuszczów ( w tym nienasyconych kwasów tłuszczowych ) , 10 % błonnika .
Nie nadają się do zbyt długiego przechowywania – ze względu na wysoką zawartość tłuszczów , które łatwo jełczeją , nadając gorzki smak .
Czas przechowywania płatków owsianych od chwili wyprodukowania wynosi od czterech do sześciu miesięcy .
