Jednak ich produkcja jest niewielka ze względu na możliwość rozwoju pleśni w czasie słodowania , a co za tym idzie konieczność zachowania bardzo dużej czystości linii technologicznej .
Sztuka kulinarna .
Płatki owsiane wykorzystuje się do sporządzania owsianek , kisielu owsianego , jako składnik muesli , granoli i rozmaitych batoników .
Wypieka się z nich ciasta ( np .
parkin ) , ciasteczka ( np .
`` anzac biscuits '' , różne makaroniki ) , spody do mazurków i kruszonki .
Dodaje się je do wypiekanego pieczywa .
Wykorzystywane są jako składnik kotletów wegetariańskich i panierek .
Rzadziej przyrządza się z nich placki typu `` pancakes '' oraz racuchy .
Regionalnie wytwarza się z nich zakwas na żur .
