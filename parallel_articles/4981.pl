Haywards Heath
Haywards Heath – miasto w Anglii w hrabstwie West Sussex , położone 19 km od Brighton i 24 km na południe od lotniska Gatwick .
Miasto zamieszkuje 22 800 osób .
Historia .
Nazwa wywodzi się ze staroangielskiej nazwy ogrodzenia żywopłotem .
Do XIX wieku osada była wsią , rozwinęła się dopiero po oddaniu do użytku kolei Londyn-Brighton w roku 1841 .
Kolejny renesans miasta zanotowano po wybudowania lotniska Gatwick .
