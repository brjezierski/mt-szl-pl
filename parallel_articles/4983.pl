Zając wielkanocny
Zając wielkanocny – jeden ze świeckich symboli Wielkiej Nocy , święta chrześcijan .
Pomimo że pierwsze interpretacje znaczenia zająca w chrześcijaństwie pochodzą z pism pisarzy wczesnochrześcijańskich ( m.in .
Hezydiusz , Augustyn z Hippony ) , to najstarsze źródła łączące zająca z symboliką jajka wielkanocnego pochodzą z końca XVII wieku .
Wyjaśnień takiego połączenia jest wiele – nawiązanie do zwyczajów podatkowych ( składanie daniny w naturze ) , kojarzenie zwierzęcia z nadejściem wiosny czy utożsamianie zająca z tym stworzeniem , które według rodziców , przynosi dla dzieci pisanki jako drobne upominki .
Popularność symbolu zająca znacznie wzrosła wraz z pojawieniem się kartek pocztowych ze świątecznymi życzeniami .
