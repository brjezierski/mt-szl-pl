Zającowate
Zającowate , zające ( Leporidae ) – rodzina ssaków z rzędu zajęczaków ( Lagomorpha ) obejmująca około 60 gatunków .
Występowanie .
Zającowate zamieszkują w stanie naturalnym wszystkie kontynenty poza Australią i Antarktydą .
Na kontynent australijski i Nową Zelandię zostały wprowadzone przez człowieka .
Tryb życia .
Zwierzęta kolonijne , średniej wielkości .
Zającowate zwykle poruszają się skokami , osiągają szybkość do 70 km/h .
Do polowań na szybko biegające zające wykorzystywano np .
charty .
Często zamieszkują nory , czasem załomy skalne , nie gromadzą zapasów na zimę i nie hibernują .
Gatunki borealne zmieniają na zimę barwę na białą .
Budowa ciała .
Cechą charakterystyczną zajęczaków jest to , iż tylne kończyny są znacznie dłuższe od przednich .
Posiadają długie , szpiczaste uszy , krótki , puszysty ogon .
W górnej szczęce mają 2 pary siekaczy , z których pierwsza stale rośnie i wymaga ścierania .
