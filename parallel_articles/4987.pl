Halicz ( stacja kolejowa )
Halicz ( , ) – stacja kolejowa w miejscowości Halicz , w rejonie iwanofrankiwskim , w obwodzie iwanofrankiwskim , na Ukrainie .
Położona jest na linii Lwów – Czerniowce .
Stacja powstała w czasach Austro-Węgier na drodze żelaznej lwowsko-czerniowiecko-suczawskiej , pomiędzy stacjami Bursztyn i Jezupol .
Dawniej zaczynała się tu linia do Podwysokiego , gdzie łączyła się z linią Stryj - Tarnopol .
