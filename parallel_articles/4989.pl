Kubeł na śmieci
Kubeł na śmieci – pojemnik zrobiony zazwyczaj z metalu lub tworzywa sztucznego , używany do czasowego gromadzenia odpadów .
Domowe kubły na odpady stoją zazwyczaj pod zlewozmywakiem w kuchni .
Wrzuca się do nich zazwyczaj różnego rodzaju odpady kuchenne .
Obecnie często do kubłów na śmieci wkłada się torby plastikowe .
Pozwala to na utrzymywanie kubła w czystości , a wyrzuca się do śmietnika torbę ze śmieciami .
W pokojach zazwyczaj stawia się bardziej estetyczne pojemniki na śmieci , do których wrzuca się np .
papiery .
Pojemniki te mają czasami pedał do uchylania pokrywy ( jeżeli pokrywa jest częścią pojemnika ) .
Od czasu ataków terrorystycznych w 1995 roku we Francji publiczne pojemniki na śmieci zostały zastąpione przezroczystymi torbami plastikowymi , w których trudno ukryć ładunek wybuchowy .
Podobnie w Wielkiej Brytanii od czasów ataków terrorystycznych jest coraz mniej koszy w halach lotnisk i na stacjach kolejowych .
W wielorodzinnych budynkach mieszkalnych , szczególnie tych wyższych , konstruuje się zsypy na śmieci , do których wysypywane są śmieci z kubła .
Jednak najczęściej zawartość kubła wynosi się do śmietnika , a śmieci wywozi specjalistyczny pojazd – śmieciarka .
Segregowanie śmieci .
Coraz bardziej popularne jest segregowanie śmieci w celu dalszego przetwarzania ( makulatura , szkło , plastik , metale i odpady organiczne ) .
Wymaga to używania kilku pojemników na śmieci , odpowiednio oznaczonych .
Kolorowe kosze na segregacje .
Kolory pojemników na odpady w Polsce zostały określone w Rozporządzeniu Ministra Klimatu i Środowiska z dnia 10 maja 2021 r. w sprawie sposobu selektywnego zbierania wybranych frakcji odpadów .
