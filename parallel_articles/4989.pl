`` trash can '' , `` recycle bin '' ) w systemach operacyjnych ( Microsoft Windows , Apple Macintosh , BeOS i inne ) jest to folder systemowy , który przechowuje pliki usunięte przez użytkownika – czasami przez nieuwagę .
