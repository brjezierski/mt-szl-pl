Hebdów
Hebdów – wieś w Polsce położona w województwie małopolskim , w powiecie proszowickim , w gminie Nowe Brzesko , przy drodze krajowej nr 79 .
W latach 1975–1998 miejscowość leżała w województwie krakowskim .
Wieś `` Chebdów '' , była własnością opactwa norbertanów w Hebdowie .
W drugiej połowie XVI wieku położona była w powiecie proszowskim województwa krakowskiego .
Historia .
Klasztor powstał prawdopodobnie w pierwszej połowie XII wieku .
Jeden z kościelnych dzwonów nosił datę 1108 r. Według publikacji Antoniego Kraszowskiego z 1752 roku , do połowy XVIII wieku kroniki klasztorne liczyły 38 opatów .
Pośród nich byli : Melchior Olszewski , sekretarz króla Zygmunta Augusta ; Jordan z Zakliczyna ; Gotard Tyzenhauzen , późniejszy biskup smoleński ; Ludwik Stępkowski ; Erazm Kretkowski ; Aleksander Trzebiński ; Tomasz Leżeński oraz Michał Wilkowski .
