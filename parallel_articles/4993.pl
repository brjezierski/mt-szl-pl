Włośnianka węglolubna
Włośnianka węglolubna ( `` Hebeloma anthracophilum '' Maire ) – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hebeloma , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim : `` Hebelomatis anthracophilum '' ( Maire ) Locq .
1979
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Występowanie .
W Polsce do 2003 r. podano tylko 2 stanowiska .
Grzyb mikoryzowy .
Rośnie na pogorzeliskach , spalonych resztkach drewna w lasach liściastych i iglastych , ogrodach , zaroślach , alejach i łąkach .
Gatunki podobne .
Istnieje wiele podobnie ubarwionych gatunków włośnianek – mają przeważnie dość podobne , cielistobeżowe ubarwienie .
Ich prawidłowe oznaczenie bez użycia mikroskopu i znajomości cech mikroskopowej budowy jest trudne .
W przypadku włośnianki węglolubnej pomocne może być miejsce występowania .
