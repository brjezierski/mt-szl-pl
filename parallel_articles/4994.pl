Włośnianka odrażająca
Włośnianka odrażająca ( `` Hebeloma fastibile '' ( Pers . )
P .
Kumm . )
– gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Według Index Fungorum takson niepewny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hebeloma , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Agaricus fastibilis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Paul Kummer w 1871 r ..
Nazwę polską nadał Stanisław Domański w 1955 r. , wcześniej Feliks Berdau opisywał ten takson pod nazwą bedłka odrażająca .
