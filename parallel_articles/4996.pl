Włośnianka późnojesienna
Włośnianka późnojesienna ( `` Hebeloma hiemale '' Bres . )
– gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hebeloma , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1898 r. Giacomo Bresàdola .
Synonimy :
Polską nazwę zarekomendował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 1,5–3,5 cm , lekko stożkowo-wypukły lub kopulasto-wypukły .
Brzeg początkowo podwinięty , potem prosty , powierzchnia gładka , tłustawa , różowawo-płowa , żółtawo-płowa , na brzegu bladokremowa z białą obwódką o szerokości kilku mm .
W eksykatach żółtawobrązowy , niewyraźnie dwukolorowy .
