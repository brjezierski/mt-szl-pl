Heerlen ( stacja kolejowa )
Heerlen ( ned : `` Station Heerlen '' ) – stacja kolejowa w Heerlen , w prowincji Limburgia , w Holandii .
Została otwarta 1 stycznia 1896 roku na linii Sittard – Herzogenrath .
Stacja była ważna dla górnictwa , dopóki nie zamknięto kopalni .
Od Heerlen , można dostać się do Akwizgranu i niektórych innych miejsc w Niemczech , w pobliżu granicy z Holandią .
Pociągi Intercity łączą Heerlen z Eindhoven i lotniskiem Schiphol .
W grudniu 2012 rozpoczęto budowę nowej stacji w ramach projektu `` Maankwartier '' .
