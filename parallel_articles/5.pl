.ag
.ag – krajowa domena internetowa najwyższego poziomu przypisana dla stron internetowych z Antigui i Barbudy , jest aktywna od 1991 roku i administrowana przez Uniwersytet Nauk Medycznych Antigui .
Domeny drugiego poziomu .
W języku niemieckim skrót AG oznacza spółkę akcyjną ( od ) i jest często pod tym względem wykorzystywana .
Nadmienić należy , że w lipcu 2004 roku niemiecki sąd ustalił , że domeny `` .ag '' mogą rejestrować jedynie spółki akcyjne , ale i tak tylko takie domeny , jaka jest nazwa firmy .
Powoduje to , że firma produkująca oprogramowanie ( SAP AG ) nie może zarejestrować domeny `` .software.ag '' .
Ag to również symbol srebra i często jest wykorzystywana przez firmy , które handlują lub wykorzystują srebro .
Korzystają z niej również strony związane z agroturystyką .
Ponieważ wiele słów w języku angielskim kończy się na -ag jest ona również wykorzystywana pod tym kątem .
