Heidevolk
Heidevolk – holenderska grupa muzyczna wykonująca folk metal .
Powstała w 2002 roku , początkowo funkcjonując pod nazwą Hymir .
Zespół w swej twórczości nawiązuje do germańskiej mitologii oraz historii Geldrii , prowincji z której pochodzi Heidevolk .
Pierwszy singel grupy `` Het Gelders Volkslied '' ukazał się w 2004 roku nakładem zespołu .
11 marca 2005 roku ponownie nakładem zespołu ukazał się album zatytułowany `` De Strijdlust is Geboren '' .
22 marca 2007 ukazał się natomiast pierwszy minialbum Heidevolk pt .
`` Wodan Heerst '' zarejestrowany w Oldenburgu w studiu nagrań Tonmeisterei , gdzie zespół współpracował z producentem muzycznym Nico van Montfortem .
31 marca 2008 roku ukazał się album `` Walhalla Wacht '' wydany nakładem wytwórni muzycznej Napalm Records .
