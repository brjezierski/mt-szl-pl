Heilbronn
Heilbronn – miasto na prawach powiatu w południowo-zachodnich Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , siedziba regionu Heilbronn-Franken oraz powiatu Heilbronn .
Leży nad rzeką Neckar , liczy 125 613 mieszkańców ( 2021 ) .
Historia .
Heilbronn po raz pierwszy wzmiankowane jest w 747 r. , prawa miejskie uzyskało w 1298 r. Niezależne miasto Starej Rzeszy od 1371 r. W 1633 r. zawiązano tu konfederację państw sprzymierzonych ze Szwecją ( `` Heilbronner Bund '' ) .
Nalot 4 grudnia 1944 zniszczył całe stare miasto i w znacznym stopniu inne części miasta .
