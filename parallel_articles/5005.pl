Heinz Fischer
Heinz Fischer ( wym .
; ur .
9 października 1938 w Grazu ) – austriacki polityk , działacz Socjalistycznej Partii Austrii ( SPÖ ) , w latach 1983–1987 minister nauki , długoletni poseł do Rady Narodowej , a od 1990 do 2002 jej przewodniczący .
W latach 2004–2016 prezydent Austrii .
Życiorys .
Ukończył studia prawnicze na Uniwersytecie Wiedeńskim ( 1961 ) , habilitował się w zakresie nauk politycznych w 1978 na Leopold-Franzens-Universität Innsbruck .
W 1993 został profesorem tej uczelni .
Odbył praktyki sądowe , po czym od 1962 pracował we frakcji Socjaldemokratycznej Partii Austrii w parlamencie federalnym , od 1963 jako sekretarza klubu SPÖ .
W 1971 po raz pierwszy został wybrany do Rady Narodowej XIII kadencji .
Do izby niższej austriackiego parlamentu uzyskiwał dziewięciokrotnie reelekcję w wyborach w 1975 , 1979 , 1983 , 1986 , 1990 , 1994 , 1995 , 1999 i 2002 .
Od maja 1983 do stycznia 1987 był ministrem nauki i badań naukowych w rządzie Freda Sinowatza .
W latach 1987–1990 kierował klubem parlamentarnym socjaldemokratów .
W listopadzie 1990 objął stanowisko przewodniczącego Rady Narodowej , zajmował je nieprzerwanie do grudnia 2002 , następnie do czerwca 2004 był wiceprzewodniczącym ( drugim przewodniczącym ) tej izby .
W wyborach prezydenckich z 25 kwietnia 2004 , przeprowadzonych w związku z upływem drugiej kadencji Thomasa Klestila , zdobył 52,39 % głosów , wygrywając ze wspieraną przez Austriacką Partię Ludową Benitą Ferrero-Waldner .
Urząd przejął 8 lipca 2004 od czasowo wykonujących obowiązki prezydenta członków prezydium Rady Narodowej ( Thomas Klestil zmarł dwa dni wcześniej ) .
W wyborach prezydenckich z 25 kwietnia 2010 z powodzeniem ubiegał się o reelekcję , otrzymując 79,33 % głosów .
Drugą kadencję prezydencką zakończył 8 lipca 2016 .
Odznaczenia .
W 1997 odznaczony Krzyżem Komandorskim z Gwiazdą Orderu Zasługi Rzeczypospolitej Polskiej .
