Helena Vondráčková
Helena Vondráčková ( ur .
24 czerwca 1947 w Pradze ) – czeska piosenkarka , aktorka i prezenterka telewizyjna .
Uchodzi za najpopularniejszą czeską artystkę XX wieku , nazywana jest „ królową czeskiej piosenki ” .
Wydała kilkadziesiąt albumów ( solowe , kompilacje , kolekcje , składanki itp . ) .
Wiele z nich zostało nagrodzonych srebrnymi , złotymi i platynowymi płytami .
W 2007 otrzymała od światowej wytwórni Universal Music diamentową płytę za całokształt kariery .
Sprzedaż jej albumów szacuje się na poziomie prawie 200 mln sztuk , co plasuje ją w czołówce światowych gwiazd muzyki .
Młodość .
Jest siostrą aktora i piosenkarza Jiříego Vondráčka oraz ciotką aktorki i piosenkarki Lucie Vondráčkovej .
W dzieciństwie uczyła się gry na fortepianie .
Kariera muzyczna .
Karierę rozpoczęła w 1964 , wygrywając konkurs „ Szukamy nowych talentów ” za interpretację piosenki George ’ a Gershwina „ The Man I Love ” i nagrywając debiutancki utwór „ Červená řeka ” .
W 1965 zdobyła nagrodę Złotego Słowika , co pozwoliło jej rozwinąć karierę .
W 1968 została wokalistką zespołu Golden Kids , który został rozwiązany dwa lata później z powodu sytuacji politycznej w kraju .
Od 1970 występuje solo .
Koncertowała nie tylko w swoim kraju , ale także poza jego granicami , m.in .
w Stanach Zjednoczonych , Japonii , Kanadzie , Brazylii , Australii oraz we wszystkich krajach Europy .
Dwukrotnie wystąpiła w Carnegie Hall w Nowym Jorku .
Krytyk „ New York Timesa ” nazwał ją „ drugą Barbrą Streisand ” .
Nagrała kilkaset programów telewizyjnych .
Występowała również w wielu musicalach , począwszy od partii Fantyny w czeskiej wersji musicalu `` Les Misérables '' z 1992 .
Ukoronowaniem jej związków z musicalem jest `` The Broadway Album '' z najsłynniejszymi musicalowymi hitami .
Ukazał się w wersji angielskiej i czeskiej , zostając wysoko doceniony przez krytyków .
Otrzymała wiele prestiżowych nagród w Czechach ( wcześniej w Czechosłowacji ) oraz w innych krajach świata .
Przez wszystkie lata swojej kariery nagrała ponad tysiąc piosenek .
Ma w swoim repertuarze światowe standardy .
