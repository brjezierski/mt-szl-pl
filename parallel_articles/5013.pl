Helminthosporium solani
Helminthosporium solani Durieu & amp ; Mont .
– gatunek workowców z klasy `` Dothideomycetes '' .
Wywołuje chorobę parch srebrzysty ziemniaka .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helminthosporium , Massarinaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1849 r. Michel Charles Durieu de Maisonneuve i Jean Pierre François Camille Montagne .
Synonimy :
Znana jest tylko anamorfa , jednak na podstawie budowy molekularnej oraz badań biochemicznych i ultrastrukturalnych zaliczony został do workowców .
Morfologia .
Na porażonych bulwach ziemniaka `` H. solani '' w sprzyjających warunkach ( wilgotność względna powyżej 90 % i temperatura powyżej 3 °C ) tworzy nalot złożony z konidioforów i konidiów .
Konidia są buławkowate , ciemne , podzielone na 5–9 grubościennych komórek o wymiarach 29–66 × 6–12 μm .
Parch srebrzysty ziemniaka może być mylony z antraknozą ziemniaka wywoływaną przez `` Colletotrichum coccodes '' .
Jednak na nalocie wywołanym przez `` H. solani '' występują konidiofory i konidia , zaś na nalocie `` C. coccodes '' mikrosklerocja .
Sprawcę choroby można skutecznie rozpoznać po morfologii zarodników używając lupy o powiększeniu 10 × lub mikroskopu .
`` H. solani '' jest pasożytem tylko ziemniaka .
Głównym źródłem zakażenia bulw ziemniaka są porażone sadzeniaki .
Znajdujący się na nich patogen wytwarza konidia , które infekują nowo powstające bulwy ziemniaków .
Jednak patogen może jako saprotrof przetrwać na wielu roślinach .
Stwierdzono jego występowanie na starzejących się liściach lucerny , sorga , żyta , owsa , kukurydzy , pszenicy , rzepaku , koniczyny czerwonej i gryki .
