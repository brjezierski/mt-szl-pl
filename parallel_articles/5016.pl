Helsinki ( album )
Helsinki – drugi studyjny album Darii Zawiałow , który ukazał się 8 marca 2019 roku .
Album dotarł do 1 miejsca listy OLiS i przebywał na niej 82 tygodni .
Osiągnął status podwójnie platynowej płyty sprzedając się w nakładzie ponad 60 tys .
