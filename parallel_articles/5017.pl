Piestrzyca czarna
Piestrzyca czarna ( `` Helvella atra '' J. König ) – gatunek grzyba z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hebvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1770 r. Johann Gerhard König i jest do tej pory obowiązująca .
Synonim : `` Leptopodia atra '' ( J. König ) Boud .
Polską nazwę podaje praca M.A .
Chmiel z 2006 r .
Morfologia .
Gatunek ten jest bardzo zmienny pod względem wielkości .
Owocniki o wysokości do 8 cm , składające się z siodłowato wygiętego kapelusza i trzonu .
Kapelusz ma średnicę od 0,5 do 3 cm , wysokość od 0,5 do 2 cm i zwykle składa się z dwóch wywiniętych do góry płatów .
