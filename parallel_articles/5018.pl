Helvella monachella
Helvella monachella ( Scop . )
Fr .
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1772 r. Joannes Antonius Scopoli , nadając mu nazwę `` Phallus monachella '' ( jako `` monacella '' ) .
Obecną nazwę nadał mu Elias Fries w 1822 r .
Synonimy :
Morfologia .
Składający się z kapelusza i trzonu .
Kapelusz siodłowaty , złożony z trzech trójkątnych płatków tworzących piramidalną , mniej lub bardziej pofałdowaną główkę .
