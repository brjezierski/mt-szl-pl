Helvella phlebophora
Helvella phlebophora Pat .
& amp ; Doass .
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1886 r. Narcisse Théophile Patouillard i Jacques Emile Doassans .
Nadana przez nich nazwa jest ważna do dzisiaj .
Synonimy :
Morfologia .
Składający się z kapelusza i trzonu .
Kapelusz o średnicy 1–3 ( -4,5 ) cm , początkowo o kształcie piramidalnym , nieregularnie pofalowany , wygięty , lekko podwinięty w kierunku trzonu , ale nigdy nie zrośnięty z nim , potem nieco bardziej rozpostarty .
Powierzchnia dolna gładka , szara , szaroczarna , niebieskoszara , górna nieco jaśniejsza , z podłużnymi bruzdami .
Trzon brudnobiaławy do ochrowożółtego z podłużnymi bruzdami .
Worki 220-280 × 12–17 μm , cylindryczne , 8-zarodnikowe , jednorzędowe .
Parafizy cylindryczne , z wierzchołkami pogrubionymi do 7 μm , septowane .
Askospory 14,5–18 × 10–12,5 μm , elipsoidalne , szkliste , gładkie , z dużą , centralną gutulą .
Wśród gatunków występujących w Polsce nieco podobna jest piestrzyca zatokowata ( `` Helvella lacunosa '' ) .
Występowanie i siedlisko .
`` Helvella phlebophora '' znana jest w niektórych krajach Europy , podano też jedno stanowisko w Rosji nad Morzem Czarnym .
Jest bardzo rzadki .
W Polsce do 2006 r. w piśmiennictwie naukowym podano tylko jedno stanowisko , w późniejszych latach podano kilka nowych jego stanowisk .
Aktualne stanowiska podaje także internetowy atlas grzybów .
Znajduje się w nim na liście gatunków zagrożonych i wartych objęcia ochroną .
Grzyb naziemny .
Występuje w lasach liściastych , zwłaszcza z bukami , dębami , brzozami , leszczyną , grabem , wzdłuż zacienionych przydroży na glebach porośniętych mchem , na szczątkach roślin , glebach żyznych , wapiennych .
Owocniki tworzy od wiosny do późnego lata , przeważnie pojedynczo , rzadko gromadnie .
