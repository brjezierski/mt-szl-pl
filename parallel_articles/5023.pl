Białogrzybówka ługowata
Białogrzybówka ługowata ( `` Atheniella delectabilis '' ( Peck ) Lüderitz & amp ; H. Lehmann ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Atheniella , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał go w 1875 r. Charles Horton Peck , nadając mu nazwę `` Agaricus delectabilis '' .
Obecną nazwę nadali mu M. Lüderitz i H. Lehmann w 2018 r .
Ma 12 synonimów .
Niektóre z nich :
F. Kwieciński w 1896 r. nadał mu polską nazwę pępkowiec wysmukły , Władysław Wojewoda w 2003 r. zmienił ją na białogrzybówka ługowata .
