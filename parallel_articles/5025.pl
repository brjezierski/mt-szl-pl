Hemimycena pithya
Hemimycena pithya ( Pers . )
Dörfelt – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Atheniella , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1801 r. Christiaan Hendrik Persoon , nadając mu nazwę `` Agaricus lacteus '' var .
`` pithyus '' .
Obecną nazwę nadał mu Heinrich Dörfelt w 1984 r .
Ma 7synonimów .
Niektóre z nich :
W checklist Władysława Wojewody w 2003 r. gatunek ten potraktowany jest jako synonim białogrzybówki igłowej ( `` Hemimycena gracilis '' ) , jednak według Index Fungorum jest to odrębny gatunek .
Występowanie i siedlisko .
Podano stanowiska tylko w niektórych krajach Europy , głównie w Europie Środkowej .
W Polsce w 2003 r. W. Wojewoda przytoczył 7 stanowisk dla `` Hemimycena gracilis '' ( łącznie z `` Hemimycena pithya '' , potraktowaną jako jej synonim ) .
Grzyb saprotroficzny .
