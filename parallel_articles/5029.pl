Henley-on-Thames
Henley-on-Thames – miasto w Wielkiej Brytanii .
w Anglii w hrabstwie Oxfordshire , położone na północnym brzegu Tamizy , ok. 16 km na północny wschód od Reading .
Znajduje się w południowo-wschodnim narożniku hrabstwa w pobliżu Berkshire i Buckinghamshire .
Populacja miasta wynosi 10 646 mieszkańców .
W mieście odbywają się królewskie regaty wioślarskie .
