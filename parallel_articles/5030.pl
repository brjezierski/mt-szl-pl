Henrik Lundqvist
Henrik Lundqvist ( ur .
2 marca 1982 w Åre ) – szwedzki hokeista , reprezentant Szwecji , trzykrotny olimpijczyk .
Życie prywatne .
Rodzicami Henrika i jego brata bliźniaka Joela są Eva Johansson i Peter Lundqvist .
Obaj dorastali w Åre , Jämtland , obszar , gdzie narciarstwo alpejskie jest najbardziej popularnym sportem , ale Henrik i Joel postanowił uprawiać inną popularną dyscyplinę sportową , którą jest hokej .
Podczas zimy nauczyciele przedszkola wykorzystywali zamarzniętą piaskownicę ( 9 m x 3 m ) na świeżym powietrzu , gdzie bliźnięta nieraz jeździli .
Ich pasja do hokeja wzrosła jeszcze bardziej , gdy ojciec Peter wziął bliźniaki , aby zobaczyć Västra Frölunda , który grał HC w Scandinavium .
W 1990 roku Henrik i Joel trafili do klubu Järpens IF .
Podczas jednej z lekcji praktyki , kiedy trener zapytał , czy ktoś chciał być bramkarzem , Joel złapał i podniósł rękę Henrik i powiedział , że jego brat chciałby .
W 1993 roku rodzina przeniosła się do Båstad , Scania , w południowej Szwecji w celu wspierania kariery tenisowej ich starszej siostry Gabrielli .
Ma żonę Therese i córkę Charlise ( ur .
2012 ) .
Kariera .
Wychowanek klubu Järpens IF .
W latach 1998-2005 był zawodnikiem Frölunda ( początkowo w drużynie juniorskiej ) w rozgrywkach Elitserien .
W drafcie NHL z 2000 został wybrany przez New York Rangers .
W lidze NHL występuje od 2005 w barwach nowojorskiej drużyny .
W 2008 podpisał sześcioletni kontrakt z klubem .
W grudniu 2013 przedłużył kontrakt o siedem lat .
Po 15 latach w Nowym Jorku w październiku 2020 przeszedł do Washington Capitals .
W barwach tego klubu nie występował jednak w sezonie NHL ( 2020/2021 ) z powodu problemów zdrowotnych z sercem .
W sierpniu 2021 ogłosił zakończenie kariery zawodniczej .
Uczestniczył w turniejach mistrzostw świata 2003 , 2004 , 2005 , 2008 , 2017 , 2019 , Pucharu Świata 2004 , 2016 , zimowych igrzysk olimpijskich 2006 , 2010 , 2014 .
W trakcie kariery zyskał pseudonim `` King Henrik '' .
