Henryk Abbe
Henryk Abbe ( ur .
15 września 1920 w Bydgoszczy , zm .
10 października 2003 w Poznaniu ) – polski aktor , głównie teatralny , także filmowy i telewizyjny .
Życiorys .
W teatrze zadebiutował 30 listopada 1946 roku .
W roku 1949 ukończył Państwową Wyższą Szkołę Teatralną w Warszawie z tymczasową siedzibą w Łodzi .
Teatr .
