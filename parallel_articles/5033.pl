Współpracował m.in .
z takimi reżyserami , jak : Zbigniew Bogdański , Maryna Broniewska , Ryszard Bugajski , Stanisław Bugajski , Jadwiga Chojnacka , Izabella Cywińska , Jerzy Goliński , Adam Hanuszkiewicz , Zygmunt Hübner , Henryk Kluba , Zbigniew Koczanowicz , Wiesław Komasa , Andrzej Konic , Krystyna Meissner , Tadeusz Minc , Krzysztof Nazar , Janusz Nyczak , Marek Okopiński , Kazimierz Opaliński , Bohdan Poręba , Ludwik René , Janusz Rzeszewski , Leon Schiller , Maciej Wojtyszko .
