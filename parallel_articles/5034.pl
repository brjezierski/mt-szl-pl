Henryk Jaroszewicz
Henryk Michał Jaroszewicz ( ur .
1974 ) – śląski językoznawca , socjolingwista i specjalista od języków południowosłowiańskich .
Doktor hab .
nauk humanistycznych , adiunkt Instytutu Filologii Słowiańskiej Wydziału Filologicznego Uniwersytetu Wrocławskiego .
Życiorys .
Urodził się w 1974 r. w Gliwicach .
Ukończył Technikum Kolejowe w Gliwicach ( 1995 ) oraz Uniwersytet Opolski w Opolu ( 1999 ) .
Na opolskiej uczelni obronił pracę doktorską `` Nowe tendencje normatywne w standardowych językach chorwackim i serbskim '' ( 2003 ) .
Był asystentem , a potem adiunktem w Katedrze Slawistyki działającej w ramach Instytutu Filologii Polskiej na Wydziale Filologicznym Uniwersytetu Opolskiego .
W latach 2001–2004 pełnił funkcję lektora i wykładowcy języka polskiego na Uniwersytecie w Zagrzebiu .
Od 2005 r. jest związany z Uniwersytetem Wrocławskim , gdzie w 2017 r. uzyskał stopień doktora habilitowanego na podstawie monografii `` Derywacja frazeologiczna w języku polskim i serbskim '' .
Obecnie jest zatrudniony na stanowisku adiunkta w Instytucie Filologii Słowiańskiej na Wydziale Filologicznym Uniwersytetu Wrocławskiego .
Jest socjolingwistą , serbokroatystą , polonistą , autorem czterech monografii oraz przeszło trzydziestu artykułów naukowych i rozdziałów monografii .
Publikuje felietony popularnonaukowe na portalu internetowym Wachtyrz.eu .
Bada współczesną historię języków południowosłowiańskich i zachodniosłowiańskich , zajmuje się polityką językową i ekologią językową , poświęca uwagę badawczą pograniczom gramatyki oraz frazeologii .
Publicznie deklaruje się jako Ślązak , jest członkiem Stowarzyszenia Osób Narodowości Śląskiej
