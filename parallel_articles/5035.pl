Henryk Jerzy Chmielewski
Henryk Jerzy Chmielewski , ps .
Papcio Chmiel ( ur .
7 czerwca 1923 w Warszawie , zm .
22 stycznia 2021 tamże ) – polski grafik , rysownik i publicysta , autor serii komiksowej „ Tytus , Romek i A ’ Tomek ” , powstaniec warszawski .
Kawaler Orderu Orła Białego .
Życiorys .
Urodził się i mieszkał w kamienicy nazywanej Piwnicą Gdańską przy ul .
Nowomiejskiej 18/20 m. 7 w Warszawie .
Zniszczony w 1944 budynek nie został odbudowany po wojnie ( na jego miejscu znajduje się zrekonstruowany Barbakan ) .
W latach 1930–1936 uczęszczał do Publicznej Szkoły Powszechnej nr 3 przy ul .
Rybaki 32 .
W 1936 rozpoczął naukę w prywatnym męskim Gimnazjum im .
Stefana Żeromskiego Towarzystwa Przyjaciół Polskich Szkół Średnich przy ul .
Marszałkowskiej 150 .
Był członkiem 70 Warszawskiej Drużyny Harcerskiej .
Opuścił miasto 7 września 1939 , po radiowym apelu Romana Umiastowskiego , i dotarł do Łukowa .
Wrócił do Warszawy w październiku 1939 .
