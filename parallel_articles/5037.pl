Heraklion
Heraklion , także Iraklion ( gr .
Ηράκλειο , `` Iraklio '' ) , d. Kandia – największe miasto i stolica administracyjna Krety oraz stolica jednostki regionalnej Heraklion .
Piąte co do wielkości miasto w Grecji , liczące 140 730 mieszkańców według spisu z 2011 roku .
Heraklion był najszybciej rozwijającym się kierunkiem turystycznym w Europie w 2017 r. , według Euromonitor , z 11,2 % wzrostem liczby przyjazdów międzynarodowych .
Według rankingu miasto znalazło się na 19. miejscu najczęściej odwiedzanych regionów w Europie za rok 2018 , z 3,4 mln odwiedzających .
