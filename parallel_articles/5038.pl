Herbert George Wells
Herbert George Wells ( ur .
21 września 1866 w Bromley , zm .
13 sierpnia 1946 w Londynie ) – brytyjski pisarz i biolog .
Po napisaniu powieści takich jak `` Wojna światów '' , `` Wehikuł czasu '' i `` Wyspa doktora Moreau '' stał się jednym z pionierów gatunku science fiction .
Życiorys .
Młodość .
Urodził się 21 września 1866 roku w Bromley , hrabstwo Kent ( obecnie dzielnica Londynu ) jako czwarty , ostatni syn Josepha Wellsa i Sarah Neal .
Dzieciństwo spędził w „ Atlas House ” przy „ 47 The High Street ” – nędznej kamienicy .
Jego rodzice z trudem radzili sobie z utrzymaniem rodziny .
Ojciec , Joseph Wells , próbował swych sił prowadząc mały sklepik , pracując jako ogrodnik i jako zawodowy gracz w krykieta .
Jednak w 1877 r. złamał nogę , w związku z czym nie mógł zarabiać na życie .
