Hereford
Hereford – miasto i civil parish w Wielkiej Brytanii , w Anglii , w hrabstwie ceremonialnym Herefordshire , położone w pobliżu granicy z Walią , nad rzeką Wye .
W 2011 roku civil parish liczyła 53 516 mieszkańców .
Hereford to typowe miasteczko rolnicze ( nazwę „ Hereford ” nosi rasa bydła mięsnego ) .
Miasto to znajduje się około 26 km na wschód od granicy Walii i 37 km na zachód od miasta Worcester .
Miasto wspomniane zostało w Domesday Book ( 1086 ) .
