Soplówka jodłowa
Soplówka jodłowa ( `` Hericium alpestre '' Pers . )
– gatunek grzybów należący do rodziny soplówkowatych ( `` Hericiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hericium , Hericiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda w 1983 r. Synonimy naukowe :
Morfologia .
Ma nieregularnie kulisty lub bryłowaty kształt i osiąga długość do kilkudziesięciu cm .
Początkowo jest biały , później kremowy , w końcu ochrowy .
Do podłoża przyrasta trzonem , z którego wyrastają liczne i wielokrotnie rozgałęziające się gałązki , zakończone pęczkiem cienkich , długich i zwisających igiełek .
Elastyczny i miękki , o nieprzyjemnym zapachu , ale w smaku łagodny .
Strzępki ze sprzążkami , bezbarwne , u niektórych okazów amyloidalne .
Występują w nich żółtawe , oleiste gleocystydy .
Podstawki zgrubiałe , 4-zarodnikowe , o rozmiarach 20-25 × 4-6 µm .
Zarodniki o kształcie od szerokoelipsoidalnego do mniej więcej kolistego , grubościenne , przejrzyste , o powierzchni gładkiej lub drobno punktowanej , niektóre z kroplami .
