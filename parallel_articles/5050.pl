Herrenberg ( Badenia-Wirtembergia )
Herrenberg – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Stuttgart , w powiecie Böblingen , siedziba wspólnoty administracyjnej Herrenberg .
Leży przy granicy Schönbucha , ok. 15 km na południowy zachód od Böblingen i 30 km od Stuttgartu .
Jest czwartym miastem pod względem wielkości w powiecie i centralnym miastem dla sąsiednich gmin .
Liczba mieszkańców Herrenberg przekroczyła w 1972 liczbę 20 tys .
w związku z reformą terytorialną .
