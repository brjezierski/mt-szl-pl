Korzeniowiec jodłowy
Korzeniowiec jodłowy ( `` Heterobasidion abietinum '' ( Niemelä & amp ; Korhonen ) – gatunek grzybów z rodziny jodłownicowatych ( `` Bondarzewiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Heterobasidion , Bondarzewiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podał Władysław Wojewoda w 1999 r .
Morfologia .
Wieloletni , trwały , o kształcie półeczkowatym , rozpostarto-odgiętym lub nieregularnym , rzadko konsolowatym .
Do podłoża przyrasta bokiem lub podstawą .
Owocniki wyrastają pojedynczo lub po kilka , i wówczas mogą się zrastać nasadami .
Brzeg owocnika biały lub żółtawy , nierówny , faliście powyginany .
Korzeniowiec jodłowy tworzy najgrubsze owocniki wśród wszystkich trzech gatunków korzeniowca występujących w Polsce .
Średnica 5–10 cm , kolor czerwonobrązowy do ciemnoczerwonobrązowego , w części środkowej także czarnobrązowy .
