Heusden-Zolder
Heusden-Zolder – gmina położona w belgijskiej prowincji Limburgii niedaleko Hasselt .
Historia .
Gmina Heusden-Zolder powstała 1 stycznia 1977 roku w wyniku połączenia dwóch gmin : Heusden i Zolder .
Obecnie składa się z byłych parafii Heusden , Berkenbos i Eversel oraz z czterech parafii dawnego Zolder : Boekt , Viversel , Bolderberg i Lindeman .
Heusden .
W VIII wieku region Heusden był terenem słabo zaludnionym .
Obszar należał do parafii Bringen Paal i należał do św .
Adelardusa , opata z Corbie .
W Heusdenn na początku ósmego stulecia swoje kazania głosił św .
Wilibrord .
W X wieku obszar otrzymał nazwę `` Husdinio '' .
