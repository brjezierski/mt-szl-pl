Heyderia cucullata
Heyderia cucullata ( Batsch ) Bacyk & amp ; Van Vooren – gatunek grzybów należący do rzędu tocznikowców ( `` Helotiales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Heyderia , Cenangiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1786 roku August Johann Georg Karl Batsch , nadając mu nazwę `` Helvella cucullata '' .
Obecną nazwę nadał mu w 2005 r. B. Bacyk i N. Van Vooren .
