Morfologia .
O wysokości do 4 cm , typu apotecjum , złożone z główki i trzonu .
Główka o średnicy 0,3–0,8 ( 1,2 ) cm , ochrowo-brązowa do czerwono-brązowej , tylko lekko spłaszczona .
Trzon smukły , brązowy , matowy z białawymi plamami , miejscami nieco prześwitujący .
