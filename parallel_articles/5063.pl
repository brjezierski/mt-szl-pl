Historia .
Pierwsze wzmianki o Geniczesku pochodzą z 10 lutego 1784 roku .
W 1816 odnotowano tu istnienie 53 zagród ( domostw ) .
Prawa miejskie osada uzyskała w 1938 roku .
Pierwszego dnia inwazji Rosji na Ukrainę 24 lutego 2022 został zajęty przez wojska rosyjskie i znalazł się pod okupacją .
Tego dnia ukraiński żołnierz Witalij Skakun wysadził most w pobliżu Geniczeska , aby spowolnić wojska rosyjskie , poświęcając życie .
30 września prezydent Rosji ogłosił włączenie do Rosji obwodu chersońskiego , nieuznane przez społeczność międzynarodową .
Od 12 listopada 2022 po opuszczeniu Chersonia przez Rosjan , Geniczesk został wybrany na tymczasową administracyjną stolicę rosyjskiego obwodu chersońskiego .
