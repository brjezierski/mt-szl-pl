Hikaru Nakamura
Hikaru Nakamura właśc .
Christopher Hikaru Nakamura ( ur .
9 grudnia 1987 w Hirakacie ) – amerykański szachista pochodzenia japońskiego , arcymistrz od 2003 roku .
Kariera szachowa .
Urodził się w Japonii , w 1987 jego rodzina przeniosła się do Stanów Zjednoczonych .
W 2001 zwyciężył w turnieju juniorów w Tulsie oraz zdobył w Oropesa del Mar tytuł wicemistrza świata juniorów do lat 14 .
W 2002 podzielił I miejsce ( przed m.in .
Bartłomiejem Macieją i Bartoszem Soćko ) na Bermudach .
W 2003 podzielił I miejsce w otwartym turnieju rozegranym w Foxwoods Casino oraz został najmłodszym amerykańskim szachistą , któremu przyznano tytuł arcymistrza , bijąc tym samym rekord Bobby ’ ego Fischera ( w 2007 rekord ten został pobity przez Fabiana Caruanę ) .
W 2004 awansował do IV rundy ( 1/16 finału ) mistrzostw świata w Trypolisie , rozgrywanych systemem pucharowym .
W tym samym roku zajął również I miejsce w turnieju `` open '' w Juan Dolio oraz pokonał 4½ - 1½ Siergieja Karjakina w meczu rozegranym w Meksyku .
W bardzo udanym 2005 zdobył tytuł mistrza Stanów Zjednoczonych , zwyciężył w turnieju `` Foxwoods Open '' , podzielił I miejsce w turnieju `` World Open '' w Filadelfii oraz zajął II miejsce w turnieju młodych mistrzów w Lozannie ( w finale ulegając Andriejowi Wołokitinowi ) .
W 2006 zwyciężył w turnieju `` North American Open '' w Las Vegas .
W 2007 podzielił II miejsce ( za Władimirem Akopianem ) w silnie obsadzonym turnieju `` open '' w Gibraltarze oraz triumfował w Barcelonie .
