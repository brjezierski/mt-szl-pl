Hillary Clinton
Hillary Diane Rodham Clinton ( wym .
, ur .
26 października 1947 w Chicago ) – amerykańska polityk .
Jako żona Billa Clintona była pierwszą damą stanu Arkansas w latach 1979–1981 , 1983–1992 i pierwszą damą Stanów Zjednoczonych w latach 1993–2001 .
Pełniła urząd senatora reprezentującego stan Nowy Jork w latach 2001–2009 i 67. sekretarza stanu Stanów Zjednoczonych w latach 2009–2013 .
W prawyborach Partii Demokratycznej w 2008 roku Clinton była główną kontrkandydatką Baracka Obamy w uzyskaniu nominacji prezydenckiej , a w wyborach prezydenckich w Stanach Zjednoczonych w 2016 roku – główną kontrkandydatką Donalda Trumpa .
Hillary Rodham dorastała w Chicago .
Uczęszczała do żeńskiej uczelni wyższej Wellesley College , którą ukończyła w 1969 roku , a następnie do Yale Law School , którą ukończyła w 1973 roku , uzyskując tytuł doktora prawa .
Następnie przeniosła się do Arkansas i w 1975 roku poślubiła Billa Clintona .
W 1977 roku założyła grupę interesu non-profit `` Arkansas Advocates for Children and Families '' , której celem jest reprezentowanie interesów dzieci i ich rodzin .
W latach 1978–1980 była prezesem Legal Services Corporation .
