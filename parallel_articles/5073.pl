Himalaje
Himalaje – najwyższy łańcuch górski na Ziemi .
Łańcuch Himalajów leży w południowej Azji , ma ponad 2,5 tys .
km długości i około 250 km szerokości .
Rozciąga się na terenach należących do Pakistanu , Indii , Chin ( Tybetu ) , Nepalu i Bhutanu .
W Himalajach znajduje się aż 10 z 14 ośmiotysięczników świata .
