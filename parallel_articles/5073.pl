Najwyższy szczyt to Mount Everest o wysokości 8848 m n.p.m .
Od wschodu pasmo to ogranicza dolina Brahmaputry , a od zachodu – Indusu .
Wierzchołki Małych Himalajów sięgają średnio 2400 m n.p.m. , tylko na zachodzie 4000 m n.p.m .
Najniższe pasmo Siwalik ciągnie się wzdłuż całego systemu od Brahmaputry do Indusu , a jego wysokość nie przekracza 2000 m n.p.m .
Nazwa „ Himalaje ” pochodzi z sanskrytu ( हिमालय , `` Himālaya '' ) i znaczy „ siedziba śniegów ” ( z himá , „ zima , śnieg ” , oraz ālaya , „ siedziba , leżący ” ) .
