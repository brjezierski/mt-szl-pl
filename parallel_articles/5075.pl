Hims ( muhafaza )
Hims ( arab .
مُحافظة حمص ) – jedna z 14 jednostek administracyjnych pierwszego rzędu ( muhafaza ) w Syrii .
Jest położona w środkowej części kraju .
Graniczy od południa z Rif Dimaszk a od południowego wschodu z państwem Irak , od północnego wschodu z Dajr az-Zaur , od północy z Rakka i Hama a od zachodu z Tartus i państwem Liban .
W 2011 roku muhafaza liczyła 1 803 000 mieszkańców ; dla porównania , w 2004 było ich 1 529 402 , a w 1981 – 815 103 .
