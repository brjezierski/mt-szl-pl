( 5072 ) Hioki
( 5072 ) Hioki ( 1931 TS1 ) – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 353 dni w średniej odległości 2,91 j.a .
Została odkryta 9 października 1931 roku w Landessternwarte Heidelberg-Königstuhl w Heidelbergu przez Karla Reinmutha .
Nazwa planetoidy została nadana na cześć Tsutomu Hioki , japońskiego astronoma , odkrywcy wielu asteroid .
