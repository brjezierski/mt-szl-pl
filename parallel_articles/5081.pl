Hirosaki
– miasto w Japonii położone w prefekturze Aomori , w północnej części wyspy Honsiu .
Miasto ma powierzchnię 524,20 km² .
W 2020 r. mieszkały w nim 168 564 osoby , w 70 789 gospodarstwach domowych ( w 2010 r. 183 534 osoby , w 70 219 gospodarstwach domowych ) .
W Hirosaki rozwinął się przemysł spożywczy , drzewny , chemiczny oraz włókienniczy .
W 1949 r. powstał w mieście .
