Charakterystyka .
`` Histoplasma capsulatum '' to owalny , drożdżakowaty grzyb wywołujący histoplazmozę .
Komórki , wielkości 2-4 µm , rozwijają się przeważnie wewnątrz komórek krwi obwodowej , w makrofagach szpiku kostnego i śledziony .
`` Histoplazma capsulatum '' wzrasta dobrze na podłożach sztucznych .
Do badań diagnostycznych przesyła się krew , skrawki tkanki ( biopsja ) lub plwocinę ( przy zakażeniu płuc ) .
Z badań serologicznych wykonuje się odczyn wiązania dopełniacza .
W przebiegu zakażenia pojawia się nadwrażliwość , którą można wykazać za pomocą odczynu skórnego .
