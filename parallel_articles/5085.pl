Kraina historyczna
Kraina historyczna – region , obejmujący obszary powiązane wspólną ( często przez kilkaset lat ) historią , tradycją i kulturą , niekiedy także więzami gospodarczymi .
Układ krain historycznych jest zwykle niezależny od aktualnego podziału terytorialnego .
