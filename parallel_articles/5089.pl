Hnífsdalur
Hnífsdalur – miejscowość w północno-zachodniej części Islandii , nad fiordem Ísafjarðardjúp , u wylotu doliny o tej samej nazwie między dwoma szczytami sięgającymi 725 m n.p.m .
Położona jest około 4 km na północ od Ísafjörður , przy drodze nr 61 prowadzącej z Ísafjörður do Bolungarvík .
Wchodzi w skład gminy Ísafjarðarbær ( region Vestfirðir ) .
Na początku 2018 roku zamieszkiwało ją 208 osób .
