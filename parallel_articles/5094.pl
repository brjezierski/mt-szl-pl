Hobbit , czyli tam i z powrotem
Hobbit , czyli tam i z powrotem ( ) − powieść fantasy dla dzieci autorstwa J.R.R .
Tolkiena , wydana 21 września 1937 przez firmę Allen & amp ; Unwin .
Polskie wydanie ukazało się w 1960 w tłumaczeniu Marii Skibniewskiej .
Opowiada o wyprawie podjętej przez krasnoludów tworzących kompanię Thorina i towarzyszącego im hobbita o nazwisku Bilbo Baggins do zajmowanej przez smoka Smauga Samotnej Góry w celu odzyskania zagarniętych skarbów i dawnej siedziby krasnoludów .
Po drodze , w jaskini Golluma , Bilbo Baggins znajduje pierścień , który daje mu moc bycia niewidzialnym .
Po wydaniu powieść szybko zyskała uznanie czytelników .
Wydawca poprosił autora o kontynuację zdarzeń , która ostatecznie ukazała się jako `` Władca Pierścieni '' .
Zarówno `` Hobbit '' , jak i `` Władca Pierścieni '' powstały pod wpływem staroskandynawskich legend , którymi zajmował się Tolkien .
Analiza literaturoznawcza sugeruje , że inspiracją podczas prac nad książką mogły być między innymi islandzka Edda starsza , postać boga Radogosta , baśń o Królewnie Śnieżce czy historia narodu żydowskiego .
Okoliczności powstania utworu .
Krótko po tym , jak Tolkien skończył swoją edukację na Oksfordzie , wrócił tam , by pracować jako profesor .
W tym okresie opublikował kilka wierszy , a także własnoręcznie pisał listy od Świętego Mikołaja do swoich dzieci , umieszczając w nich rysunki gnomów i goblinów .
Nie jest znana dokładna data rozpoczęcia prac nad książką .
Tolkien zaczął prace nad `` Hobbitem '' we wczesnych latach 30 .
Pewnego dnia , sprawdzając egzaminy studentów , znalazł pustą kartkę .
Pod wpływem impulsu napisał na niej `` In a hole in the ground there lived a hobbit '' .
Fragmenty książki były czytane podczas spotkań klubu Inklingów .
