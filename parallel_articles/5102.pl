Bocznianka żółtobrązowa
Bocznianka żółtobrązowa ( `` Hohenbuehelia tremula '' ( Schaeff . )
Thorn & amp ; G.L .
Barron ) – gatunek grzybów z rodziny boczniakowatych ( `` Pleurotaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hohenbuehelia , Pleurotaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1774 r. J.Ch .
Schaeffer nadając mu nazwę `` Agaricus tremulus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1951 r. Rolf Singer , przenosząc go do rodzaju `` Hohenbuehelia '' .
W 1986 r. opisano jego anamorfę pod nazwą `` Nematoctonus geogenius '' Thorn & amp ; G.L .
Barron .
