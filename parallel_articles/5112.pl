Holyoke ( Massachusetts )
Holyoke – miasto w hrabstwie Hampden w stanie Massachusetts w Stanach Zjednoczonych , leżące między zachodnim brzegiem rzeki Connecticut a pasmem Mount Tom .
Według spisu z 2010 r. miasto liczyło 39 880 mieszkańców .
W 2019 r. szacowana populacja wynosiła 40 117 .
Położone 8 mil ( 13 km ) na północ od Springfield , Holyoke jest częścią obszar metropolitalny Springfield , jednego z dwóch odrębnych obszarów metropolitalnych w Massachusetts .
Holyoke jest jednym z pierwszych planowanych miast przemysłowych w Stanach Zjednoczonych .
