Holzkirchen ( powiat Miesbach )
Holzkirchen – gmina targowa w południowych Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , w powiecie Miesbach .
Leży około 15 km na północny zachód od Miesbach , przy autostradzie A8 , drodze B13 , B318 i liniach kolejowych : Monachium – Lenggries , Monachium - Bayrischzell , Holzkirchen - Rosenheim .
Polityka .
Wójtem gminy jest Josef Höß z CSU , rada gminy składa się z 24 osób .
