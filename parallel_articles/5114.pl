Homel
Homel ( , `` Homiel '' ; , `` Gomiel '' ) – miasto obwodowe położone w południowo-wschodniej Białorusi , nad rzeką Soż , stolica obwodu i rejonu homelskiego .
Drugie pod względem liczby ludności miasto kraju .
Liczba mieszkańców : 510,3 tys .
( 2020 )
Historia .
Średniowiecze .
Na prawym brzegu rzeki Soż w epoce żelaza funkcjonowało gród , na którego miejscu w X–XI w. wybudowano kolejny gród obronny , który należał do Rusi Kijowskiej .
W XII wieku gród leżał w granicach księstwa czernihowskiego .
Homel był przedmiotem sporów między okolicznymi książętami , przechodził kolejno w ręce książąt smoleńskich , czernihowskich i siewierskich .
W XIII był atakowany przez Mongołów .
W granicach Wielkiego Księstwa Litewskiego .
