Historia .
Nazwa `` Honiara '' pochodzi od słów `` nagho ni ara '' , które można przetłumaczyć pobieżnie jako „ miejsce wschodniego wiatru ” lub „ stawiać czoła południowo-wschodniemu wiatrowi ” w jednym z języków Guadalcanal .
