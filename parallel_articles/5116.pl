Honiton
Honiton ( IPA /hʌnɪtɘn/ lub /hɒnitɘn/ ) - miasto w Wielkiej Brytanii , w Anglii w hrabstwie Devon , położone nad rzeką Otter , 24 km na północny wschód od Exeter .
Stacja kolejowa na linii Londyn-Exeter .
Historia .
Miasto położone na rzymskim szlaku `` Fosse Way '' z Exeter do Lincoln .
Wzmiankowane w Domesday Book jako `` Honetone '' .
W czasach nowożytnych w mieście rozwinął się przemysł - tu właśnie wyprodukowano pierwszą w Anglii koronkę , której sposób wykonania przywieźli emigranci flamandzcy .
Królowa Wiktoria miała suknię ślubną ozdobioną koronkami z Honiton .
W XVIII w. miasto strawił pożar .
Ponowny rozwój nastąpił w XX w. , po wybudowaniu linii kolejowej .
