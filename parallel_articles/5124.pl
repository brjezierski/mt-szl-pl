Hopy
Hopy ( dodatkowa nazwa w j. kaszub .
Hopë ) – wieś kaszubska w Polsce , położona w województwie pomorskim , w powiecie kartuskim , w gminie Przodkowo , przy drodze wojewódzkiej nr 224 .
W latach 1954–1957 wieś należała i była siedzibą władz gromady Hopy , po jej zniesieniu w gromadzie Przodkowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
