Hordzież
Hordzież – wieś ( dawniej miasto ) w Polsce położona w województwie lubelskim , w powiecie łukowskim , w gminie Serokomla .
Hordzież uzyskał lokację miejską przed 1552 rokiem , zdegradowany przed 1562 rokiem .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Obok miejscowości przepływa Motwica , niewielka rzeka dorzecza Wisły .
14 kwietnia 1940 niemiecka ekspedycja kama dokonała pacyfikacji wsi Józefów , Bronisławów , Zakępie , Hordzież , Serokomla , Bielany i Ruda , mordując 191 osób .
W czasie pacyfikacji Niemcy spalili w tych wsiach 27 gospodarstw .
Ustalono 170 nazwisk ofiar zbrodni w tym 12 z wsi Hordzież .
