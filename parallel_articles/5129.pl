Horgoš
Horgoš ( cyr .
Хоргош ; węg .
`` Horgos '' ) – wieś w Serbii , w Wojwodinie , w okręgu północnobanackmi , w gminie Kanjiža .
Leży w pobliżu przejścia granicznego z Węgrami `` Horgoš – Röszke '' na trasie E75 .
W 2011 roku liczyła 5709 mieszkańców .
Miejscowość znana jest z uprawy papryki o tej samej nazwie .
