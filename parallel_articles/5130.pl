Ważny węzeł kolejowy .
Obecna nazwa miejscowości pochodzi od geologa rosyjskiego Piotra Gorłowa , który prowadził prace badawcze na terenie osady .
Od 2014 znajduje się pod kontrolą Donieckiej Republiki Ludowej .
Historia .
Założone w 1779 roku jako osada zasiedlenia przez ludność pochodzącą z dzisiejsze Serbii , Chorwacji i położonych na zachód rejonów Ukrainy .
Na terenie niewielkiej osady istniały chłopskie biedaszyby już w pierwszej połowie XIX wieku .
