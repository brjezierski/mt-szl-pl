Horodenka
Horodenka ( ukr .
Городенка ) – miasto w rejonie kołomyjskim , w obwodzie iwanofrankowskim , na Ukrainie .
Historycznie leży na Pokuciu .
Do 2020 stolica rejonu horodeńskiego .
W mieście rozwinął się przemysł spożywczy .
Historia .
Pierwsza pisemna wzmianka o Horodence jako wsi zamieszkanej przez rolników i rzemieślników pochodzi z 1195 roku .
W XVI wieku w Horodence osiedlili się pierwsi Żydzi , w XVII wieku - pierwsi Ormianie .
