Horodysławice
Horodysławice – wieś na Ukrainie w rejonie pustomyckim należącym do obwodu lwowskiego .
Położenie .
Własność Herburtów w połowie XVI wieku , położona była w ziemi lwowskiej .
Na podstawie Słownika geograficznego Królestwa Polskiego i innych krajów słowiańskich : Horodysławice to `` wieś w powiecie bóbreckim , 18 km na północ od miasta Bóbrka '' .
