Horst Bienek
Horst Bienek ( ur .
7 maja 1930 w Gliwicach , zm .
7 grudnia 1990 w Monachium ) – niemiecki pisarz , publicysta , tłumacz , reżyser filmowy .
Życiorys .
Dzieciństwo spędził w robotniczej części Gliwic .
W 1945 został wysiedlony do Niemiec ( decyzję o opuszczeniu Gliwic podjął samodzielnie , ponieważ zakazano używania języka niemieckiego i przestała wychodzić niemieckojęzyczna prasa ) i znalazł się w radzieckiej strefie okupacyjnej .
Pobierał nauki u Bertolta Brechta w Berlinie Wschodnim .
W 1951 został aresztowany przez NKWD za współpracę z , niemieckim dziennikarzem pracującym dla CIA , i skazany na 20 lat łagrów w Workucie ( ZSRR ) .
W oficjalnej wersji swojej biografii Bienek twierdził jednak , że zarzuty dotyczyły działania przeciwko polityce kulturalnej NRD i skazany został na 25 lat .
W 1955 na podstawie amnestii został zwolniony i osiadł w RFN , gdzie pracował jako redaktor radiowy , lektor i reżyser teatralny .
W swoich pierwszych pracach zajmował się doświadczeniami okresu łagrowego .
Później swoją literacką twórczość związał z Gliwicami i Górnym Śląskiem .
Najwyżej cenionym dziełem Bienka jest gliwicka tetralogia : `` Pierwsza polka '' , `` Wrześniowe światło '' , `` Czas bez dzwonów '' oraz `` Ziemia i ogień '' .
Akcja wspomnianych powieści toczy się w Gliwicach w okresie II wojny światowej , a w losach bohaterów odnaleźć można liczne wątki autobiograficzne .
Był osobą homoseksualną .
Zmarł na AIDS 7 grudnia 1990 w Monachium .
Uważał się za niemieckojęzycznego Ślązaka .
Gliwice odwiedził w 1987 , czterdzieści dwa lata po ich opuszczeniu , przy okazji realizacji filmu biograficznego .
Wyraził wtedy opinię , że to Niemcy sami spowodowali utratę Śląska , popierając politykę Adolfa Hitlera .
Ulicę przy której stoi jego rodzinny dom w Gliwicach Zatorzu ( niem .
`` Stadtwald '' ) nazwano jego imieniem , a na samym budynku umieszczono pamiątkową tablicę .
