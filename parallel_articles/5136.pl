Husni Mubarak
Husni Mubarak , właśc .
Muhammad Husni as-Sajjid Mubarak ( arab .
محمد حسني السيد مبارك , wymowa egipska : [ ] ; ur .
4 maja 1928 w Kafr al-Musajliha , zm .
25 lutego 2020 w Kairze ) – egipski polityk i wojskowy , prezydent Egiptu od 14 października 1981 do 11 lutego 2011 , a wcześniej oficer w lotnictwie egipskim i wiceprezydent .
Od 1950 służył w egipskich siłach powietrznych , a jako ich dowódca od 1972 wyróżnił się w wojnie Jom Kipur .
Po odejściu z wojska w 1975 został wiceprezydentem Egiptu u boku Anwara as-Sadata .
Po zabójstwie as-Sadata w 1981 przejął po nim urząd prezydencki .
Rządy Mubaraka charakteryzowały się z jednej strony represjami wobec radykalnych organizacji islamistycznych , odpowiedzialnych za zamach na poprzednika , z drugiej – liberalnymi reformami ekonomicznymi , których skutkiem był szybki rozwój gospodarczy kraju , ale i wzrost rozwarstwienia społecznego .
W polityce zagranicznej Mubarak doprowadził do przełamania izolacji Egiptu w świecie arabskim , w którą kraj popadł po podpisaniu przez as-Sadata traktatu pokojowego z Izraelem w 1979 , za cenę pogorszenia relacji z Izraelem .
Z inicjatywy Mubaraka Egipt przystąpił do I wojny w Zatoce Perskiej po stronie koalicji antyirackiej .
Na początku 2011 Mubarak został obalony w wyniku protestów ulicznych .
Po rewolucji został aresztowany wraz z synami , Alą i Dżamalem ; w 2012 został skazany na dożywotnie więzienie za krwawe tłumienie demonstracji w 2011 .
Opuścił więzienie w 2017 .
