Hoszcza ( Białoruś )
Hoszcza ( , `` Hoszcza '' ; , `` Goszcza '' ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie iwacewickim , w sielsowiecie Omelna , nad jeziorem Hoszcza .
Słownik geograficzny Królestwa Polskiego z drugiej połowy XIX wieku opisuje Hoszczę jako `` zapadły w bagnach i całkiem odludny i ubogi zakątek Pińszczyzny '' .
W dwudziestoleciu międzywojennym leżała w Polsce , w województwie poleskim , w powiecie kosowskim/iwacewickim , w gminie Święta Wola .
Po II wojnie światowej w granicach Związku Radzieckiego .
Od 1991 w niepodległej Białorusi .
