Hotboii
Javarri Latre Walker ( ur .
13 czerwca 2000 w Orlando ) , znany zawodowo jako Hotboii ( zapis stylizowany : H O T B O I I ) – amerykański raper , piosenkarz i autor tekstów .
Jego przełomowy singiel „ Do n't Need Time ” z 2020 roku miał ponad 51 milionów wyświetleń na YouTube .
Podpisał kontrakt z Interscope Records , Hitmaker Music Group i 22 Entertainment .
Wczesne życie .
Walker urodził się 13 czerwca 2000 r. w Orlando na Florydzie , dorastał w Pine Hills na zachodnim wybrzeżu jako jedno z 17 rodzeństwa .
Słuchał takich artystów jak Lil Wayne , Rich Homie Quan i Kodak Black .
Zaczął rapować w wieku siedmiu lat , zaczął wydawać muzykę w 2016 roku z pomocą swojej matki , zaczynając od przesyłania piosenek na SoundCloud i YouTube .
Problemy prawne .
Walker spędził dwa lata w poprawczaku , od 2016 do 2018 roku za włamanie .
To właśnie w tym czasie zdecydował się potraktować swoją karierę poważniej .
Kariera .
Tuż przed pobytem w poprawczaku zaczął zwracać na siebie uwagę lokalnej sceny dzięki swojej piosence „ Switcharoo ” .
Po opuszczeniu zakładu dla nieletnich w 2018 roku zaczął wydawać więcej singli , publikując we wrześniu swój pierwszy teledysk do piosenki „ Life of a Dog ” .
