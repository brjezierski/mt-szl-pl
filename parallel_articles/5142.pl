Młodość .
Lovecraft urodził się 20 sierpnia 1890 w domu nr 194 na Angell Street w Providence .
Był jedynym dzieckiem Winfielda Scotta Lovecrafta , komiwojażera sprzedającego kamienie szlachetne , i Sary Susan Lovecraft ( `` de domo '' Phillips ) .
Jego rodzice pobrali się , mając po trzydzieści lat .
Kiedy Lovecraft miał trzy lata , jego ojciec podczas spotkania służbowego w chicagowskim hotelu popadł w głęboką psychozę .
Po niedługim czasie senior powrócił do Providence .
Na miejscu został skierowany do lokalnego szpitala , gdzie pozostał aż do swojej śmierci w 1898 r .. Młody Lovecraft przez całe swoje życie podtrzymywał , że jego ojciec zmarł w wyniku powikłań po wycieńczeniu psychicznym .
Naukowcy przypuszczają , że zgon Winfielda Scotta Lovecrafta był wynikiem paraliżu ciała po zarażeniu się kiłą .
Historycy zajmujący się badaniem przeszłości H.P .
Lovecrafta nie wiedzą , czy kiedykolwiek miał on pojęcie o rzeczywistych problemach zdrowotnych swojego ojca .
Po hospitalizacji ojca Lovecraft był wychowywany przez swoją matkę , dwie ciotki ( Lillian Delorę Phillips i Annie Emeline Phillips ) oraz dziadka , Whipple ’ a Van Burena – amerykańskiego biznesmena .
W piątkę zamieszkiwali rodzinny dom w Providence .
Lovecraft był genialnym dzieckiem .
Mając trzy lata potrafił recytować poezję , w wieku sześciu lat zaczął pisać wiersze .
Dziadek zachęcał go do lektury , a ponadto wprowadził do świata grozy , opowiadając mu własne historie .
W młodości Lovecraft często chorował .
Ze względu na jego niestabilny stan zdrowia uczęszczał do szkoły podstawowej z dłuższymi przerwami , podczas których uczyli go prywatni nauczyciele .
W tym okresie intensywnie czytał , poznawał świat nauk ścisłych , a także łacinę i historię rzymską .
W 1899 zaczął wydawać amatorskie czasopismo „ The Scientific Gazette ” , które powielał domowym sposobem .
W następnych latach wydał w tej samej formie serię naukowych publikacji dotyczących fizyki , chemii i astronomii , a także biografie wybitnych postaci z historii starożytnego Rzymu .
Przetłumaczył też część „ Przemian ” Owidiusza .
W 1903 powrócił do szkoły .
