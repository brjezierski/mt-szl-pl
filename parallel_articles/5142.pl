H.P .
Lovecraft
H.P .
Lovecraft , właściwie Howard Phillips Lovecraft ( ur .
20 sierpnia 1890 w Providence , zm .
15 marca 1937 tamże ) – amerykański pisarz , autor opowieści grozy ( `` weird fiction '' ) i fantasy , twórca mitologii Cthulhu .
Jest jednym z prekursorów fantastyki naukowej , a styl przez niego stworzony określany jest jako horror lovecraftowski .
