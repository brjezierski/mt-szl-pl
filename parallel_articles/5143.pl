Howard Zieff
Howard Zieff ( ur .
21 października 1927 w Chicago , zm .
22 lutego 2009 w Los Angeles ) – amerykański reżyser filmów i reklam , a także fotograf .
W ciągu swojej reżyserskiej kariery zrealizował 9 filmów fabularnych .
Wszystkie z nich to komedie ; największą popularność przyniosły mu : `` Szeregowiec Benjamin '' ( 1980 ) z nominowaną do Oscara Goldie Hawn oraz `` Moja dziewczyna '' ( 1991 ) .
Zmarł w szpitalu w Los Angeles w następstwie komplikacji wywołanych chorobą Parkinsona .
