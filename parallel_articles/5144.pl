Hołodnica
Hołodnica – wieś w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Rokitno .
Wierni Kościoła rzymskokatolickiego należą do parafii Trójcy Świętej w Janowie Podlaskim .
Wieś szlachecka położona była w końcu XVIII wieku w powiecie brzeskolitewskim województwa brzeskolitewskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
