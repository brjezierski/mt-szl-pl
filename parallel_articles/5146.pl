Hrabstwo Arlington
Hrabstwo Arlington ( ang .
`` Arlington County '' ) – hrabstwo w stanie Wirginia w Stanach Zjednoczonych , należące do aglomeracji Waszyngtonu , położone nad rzeką Potomak .
W roku 2000 w hrabstwie mieszkało 189 453 osób .
Historia .
Pod koniec XVIII w. obecny teren Arlington został odłączony od stanu Wirginia i wszedł w skład powstałego wówczas Dystryktu Kolumbii .
W 1801 roku Dystrykt Kolumbii został podzielony na hrabstwo Waszyngton na lewym ( wschodnim ) brzegu Potomaku i hrabstwo Alexandrii , leżące na prawym ( zachodnim ) brzegu rzeki .
W skład tego ostatniego wchodziło obecne Arlington .
W 1846 roku na żądanie mieszkańców hrabstwo Alexandrii wróciło do Wirginii .
W 1852 roku południowa część hrabstwa weszła w skład miasta Alexandria .
By uniknąć pomyłek spowodowanych istnieniem dwóch sąsiadujących jednostek administracyjnych ( hrabstwa i miasta Alexandria ) w 1920 roku hrabstwo Alexandria zmieniło nazwę na hrabstwo Arlington .
Obecnie Arlington spełnia głównie funkcje mieszkaniowe .
W Arlington ma swoją siedzibę Departament Obrony Stanów Zjednoczonych – budynek Pentagon , znajduje się tu również Port lotniczy Waszyngton Ronalda Reagana , obsługujący niemal wyłącznie loty krajowe .
Arlington leży w zasięgu metra waszyngtońskiego , linii żółtej , pomarańczowej i niebieskiej .
Na terenie hrabstwa znajduje się też wojskowy Cmentarz Narodowy w Arlington .
18 stycznia 1957 przy ulicy 333 Jefferson Davis Highway otworzono Twin Bridges Motor Hotel , pierwszy hotel należący do Marriott Corporation .
Choć część hrabstwa Arlington jest pokryta typowo miejską zabudową , z formalnego punktu widzenia Arlington nie jest miastem , ani żadnych miast nie zawiera .
