Hrabstwo Colquitt
Hrabstwo Colquitt ( ang .
`` Colquitt County '' ) – hrabstwo w stanie Georgia w Stanach Zjednoczonych .
Hrabstwo nazwane na cześć Waltera Terry'ego Colquitta , amerykańskiego senatora .
Jego siedzibą administracyjną jest Moultrie .
Geografia .
Według spisu z 2000 roku obszar całkowity hrabstwa obejmuje powierzchnię 556,55 mil2 ( 1441,46 km2 ) , z czego 552,27 mil2 ( 1430,37 km2 ) stanowią lądy , a 4,28 mil2 ( 11,09 km2 ) stanowią wody .
Demografia .
Według spisu w 2020 roku liczy 45,9 tys .
mieszkańców , co oznacza wzrost o 0,9 % w porównaniu z poprzednim spisem z roku 2010 .
54,1 % stanowią białe społeczności nielatynoskie , 24 % to Afroamerykanie lub czarnoskórzy , 20,5 % to Latynosi , 1,4 % było rasy mieszanej , 1,2 % to rdzenna ludność Ameryki i 1 % to Azjaci .
Polityka .
W wyborach prezydenckich w 2020 roku , 73,2 % głosów otrzymał Donald Trump i 26 % przypadło dla Joe Bidena .
Religia .
W 2010 roku 2,9 % osób deklaruje członkostwo w Kościele katolickim .
