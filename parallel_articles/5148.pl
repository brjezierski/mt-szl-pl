Hrabstwo Currituck
Hrabstwo Currituck ( ang .
`` Currituck County '' ) – hrabstwo w stanie Karolina Północna w Stanach Zjednoczonych .
Geografia .
Według spisu z 2000 roku obszar całkowity hrabstwa obejmuje powierzchnię 526 mil2 ( 1362 km2 ) , z czego 262 mile2 ( 679 km2 ) stanowią lądy , a 264 mil2 ( 684 km2 ) stanowią wody .
Według szacunków United States Census Bureau w roku 2012 miało 24 077 mieszkańców .
Jego siedzibą administracyjną jest Currituck .
