Hrabstwo Marin
Hrabstwo Marin ( ang .
`` Marin County '' ) – hrabstwo w stanie Kalifornia w Stanach Zjednoczonych .
Obszar całkowity hrabstwa obejmuje powierzchnię 828,20 mil² ( 2145,03 km² ) .
Według szacunków United States Census Bureau w roku 2009 miało 250 750 mieszkańców .
Hrabstwo powstało w 1850 roku .
