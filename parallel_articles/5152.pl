Hrabstwo Orange ( Kalifornia )
Hrabstwo Orange ( ang .
`` Orange County '' ) – hrabstwo w stanie Kalifornia w Stanach Zjednoczonych .
Obszar całkowity hrabstwa obejmuje powierzchnię 947,98 mil² ( 2455,26 km² ) i należy do obszaru metropolitalnego Los Angeles .
Według spisu w 2020 roku liczy 3,2 mln mieszkańców .
Jest to zamożne hrabstwo od dawna znane z konserwatyzmu politycznego .
Hrabstwo Orange tworzą miejscowości na południe od Los Angeles .
Przemysł zaawansowanych technologii stał się podstawą do utworzenia w roku 1965 Uniwersytetu Kalifornijskiego w miejscowości Irvine , którego kierunki kształcenia odpowiadają zapotrzebowaniom regionu : elektronika , technologia medyczna i biologiczna .
W Orange County przemysł wysokich technologii wyprzedził więc powstanie uniwersytetu .
Orange County to także miejsce mocno związane z rozrywką .
Akcja wielu popularnych filmów i seriali została umieszczona właśnie tam ( m.in .
film `` The Orange County '' z aktorem Jackiem Blackiem czy serial telewizji FOX , `` Życie na fali '' .
W Orange County swoje początki miał zespół punkowy The Offspring .
Hrabstwo reprezentują również raper Ca $ his oraz zespoły The Adolescents , Atreyu , Ignite , Zebrahead i Avenged Sevenfold .
Hrabstwo powstało w 1889 roku .
