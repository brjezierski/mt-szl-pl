Demografia .
Według danych pięcioletnich z 2020 roku , 57,6 % mieszkańców stanowiła ludność biała ( 39,8 % , nie licząc Latynosów ) , 21,1 % to Azjaci , 6,8 % miało rasę mieszaną , 1,7 % to czarnoskórzy Amerykanie lub Afroamerykanie , 0,49 % to rdzenna ludność Ameryki i 0,3 % to Hawajczycy i mieszkańcy innych wysp Pacyfiku .
Latynosi stanowią 33,8 % ludności hrabstwa .
Do największych grup należą osoby pochodzenia meksykańskiego ( 28,7 % ) , niemieckiego ( 7,9 % ) , wietnamskiego ( 6,9 % ) , angielskiego ( 6,4 % ) , irlandzkiego ( 6,4 % ) , „ amerykańskiego ” ( 4,5 % ) , włoskiego ( 3,9 % ) , chińskiego ( 3,6 % ) i koreańskiego ( 3,1 % ) .
