Hrabstwo Ventura
Hrabstwo Ventura ( ang .
`` Ventura County '' ) – hrabstwo w stanie Kalifornia w Stanach Zjednoczonych .
Obszar całkowity hrabstwa obejmuje powierzchnię 2208,20 mil² ( 5719,21 km² ) .
Według szacunków United States Census Bureau w roku 2009 miało 802 983 mieszkańców .
Hrabstwo powstało w 1873 roku .
