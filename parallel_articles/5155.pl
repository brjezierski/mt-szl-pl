Hrachovište
Hrachovište ( ) – wieś ( obec ) w powiecie Nowe Miasto nad Wagiem , w kraju trenczyńskim , w zachodniej Słowacji , o populacji około 714 mieszkańców ( dane z 2016 ) .
Historia .
W dokumentach historycznych wieś wzmiankowana po raz pierwszy w 1392 .
Wtedy miejscowość należała do wsi Čachtice .
W 1935 Hrachovište nawiedziła wielka powódź .
Po 1945 zaczęto intensywnie odbudowywać wieś .
Geografia .
Centrum wsi leży na wysokości 210 m n.p.m .
Gmina zajmuje powierzchnię 9,204 km² .
Zasoby genealogiczne .
Zapisy badań genealogicznych przechowywane są w archiwum państwowym „ Statny Archiv w Bratysławie , na Słowacji ” .
