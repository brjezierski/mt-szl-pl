Hrubieszów
Hrubieszów ( do 1802 niekiedy `` Rubieszów '' , , `` Hrubesziw '' , ) – miasto w południowo-wschodniej Polsce , w województwie lubelskim , siedziba powiatu hrubieszowskiego oraz gminy wiejskiej Hrubieszów .
Według danych GUS z 31 grudnia 2019 r. Hrubieszów liczył 17 528 mieszkańców .
W okolicach miasta znajdują się liczne wczesnośredniowieczne cmentarzyska , kurhany i inne odkrycia archeologiczne ( m.in .
w Gródku , Masłomęczu i Kryłowie ) .
Położenie .
Miasto jest położone w ramionach rzeki Huczwy , dopływu Bugu i znajduje się zaledwie 18 km od przejścia granicznego z Ukrainą w Zosinie ( 5 km w linii prostej przez Bug ) .
Hrubieszów jest najdalej na wschód wysuniętym miastem Polski , leżącym w środku Kotliny Hrubieszowskiej ( Wyżyna Wołyńska ) .
Region głównie rolniczy w rejonie najżyźniejszych gleb Polski – czarnoziemu .
