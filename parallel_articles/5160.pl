Hu Jia
Hu Jia ( ; ur .
25 lipca 1973 w Pekinie ) – chiński działacz społeczny i dysydent w Chińskiej Republice Ludowej .
Pracuje na rzecz ruchu demokratycznego , ochrony środowiska i praw osób zarażonych HIV .
Jego żona , Zeng Jinyan , jest również aktywistką na rzecz praw człowieka i znaną chińską blogerką .
Od 17 lipca 2006 roku Hu Jia przebywał de facto w areszcie domowym w swoim pekińskim mieszkaniu .
Areszt polegał na tym , że przez całą dobę przed budynkiem albo w mieszkaniu na parterze przebywali ubrani po cywilnemu agenci policji , którzy uniemożliwiali Hu Jia opuszczanie mieszkania i śledzili jego żonę .
W grudniu 2007 roku Hu Jia został aresztowany .
W kwietniu 2008 roku skazany został na 3 lata i sześć miesięcy więzienia pod zarzutem podżegania do obalenia władzy państwowej .
Hu Jia jest autorem i jednocześnie bohaterem filmu `` Prisoners in Freedom City '' ( `` Więźniowie w Mieście Wolności '' ) .
Jest to dokument nakręcony z ukrycia w czasie , gdy Hu Jia przebywał w areszcie domowym ( w swoim mieszkaniu na osiedlu Miasto Wolności w Pekinie , stąd ironiczny tytuł ) , a jego żona była nieustannie śledzona przez agentów .
W Polsce można go obejrzeć na pokazach organizowanych przez Amnesty International .
21 kwietnia 2008 roku rada miasta Paryża przyznała Hu Jia honorowe obywatelstwo .
23 października 2008 Parlament Europejski uhonorował go Nagrodą Sacharowa .
26 czerwca 2011 roku został zwolniony z więzienia .
