Huanggang
Huanggang ( chiń .
黃冈 ; pinyin : `` Huánggāng '' ) – miasto o statusie prefektury miejskiej w środkowych Chinach , w prowincji Hubei , nad rzeką Jangcy .
W 2010 roku liczba mieszkańców miasta wynosiła 124 058 .
Prefektura miejska w 1999 roku liczyła 7 300 593 mieszkańców .
