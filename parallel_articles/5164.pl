Hubert Auriol
Hubert Auriol ( ur .
7 czerwca 1952 w Addis Abebie , zm .
10 stycznia 2021 w Garches ) – francuski motocyklista i kierowca rajdowy , uczestnik rajdów terenowych .
Trzykrotny zwycięzca Rajdu Dakar : dwukrotnie w latach 1981 i 1983 w kategorii motocykli oraz jednokrotnie w roku 1992 w kategorii samochodów .
Dwukrotnie podczas tego rajdu był drugi : w 1984 ( motocykle ) i w 1994 ( samochody ) .
W 1993 zajął trzecie miejsce w kategorii samochodów .
Życiorys .
Urodził się 7 czerwca 1952 w stolicy Etiopii – Addis Abebie .
W roku 1979 zadebiutował w I. edycji Rajdu Dakar .
Następnie został zwycięzcą tego rajdu w latach 1981 i 1983 w kategorii motocykli jadąc marką BMW .
W 1985 zajął 2. miejsce w Rajdzie Dakar na motocyklu Cagiva .
Podczas Rajdu Dakar 1987 na jednym z etapów zaliczył kraksę i złamał obydwie kostki , mimo to udało mu się ukończyć etap .
W 1988 przeniósł się na kategorię Buggy , zaś później startował w kategorii samochodów ( Mitsubishi ) .
W 1992 został pierwszym zawodnikiem , który wygrał zarówno Rajd Dakar w kategorii motocykli jak i samochodów .
