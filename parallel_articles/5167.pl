Huehuetenango ( miasto )
Huehuetenango – miasto w zachodniej Gwatemali , w górach Sierra Madre de Chiapas , nad rzeką Selegua , położone w odległości 270 km na zachód od miasta Gwatemala .
Ośrodek administracyjny departamentu Huehuetenango .
Według danych szacunkowych w 2012 roku liczba ludności miasta wyniosła 155 500 mieszkańców .
Miasto zostało założone przez Hiszpanów w 1524 po zniszczeniu przez nich miasta Zaculeu , stolicy Majów posługujących się językiem mam .
Położone niedaleko od Huehuetenango ruiny Zaculeu są obecnie dużą atrakcją turystyczną .
