Hugo Lloris
Hugo Lloris ( wym .
[ ] ; ur .
26 grudnia 1986 w Nicei ) – francuski piłkarz , występujący na pozycji bramkarza w angielskim klubie Tottenham Hotspur , którego jest kapitanem .
W latach 2008–2022 reprezentant Francji .
Złoty medalista Mistrzostw Świata 2018 , srebrny medalista Mistrzostw Świata 2022 .
Młodość i życie prywatne .
Matka Llorisa była prawniczką , a ojciec , o katalońskim pochodzeniu , bankierem .
Ma młodszego brata Gautiera , który również jest piłkarzem .
W 2008 zmarła jego matka .
Jako młodzieniec uprawiał tenis , wraz z klubowym kolegą Yoannem Gourcuffem , aż do 13 roku życia .
10 sierpnia 2010 roku znalazł się na okładce gry sportowej FIFA 11 we francuskiej wersji , wraz z Karimem Benzemą .
