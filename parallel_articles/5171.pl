Hugo ( Siemianowice Śląskie )
Hugo – dawna kolonia robotnicza oraz historyczna część Siemianowic Śląskich , położona w południowej części miasta , na obszarze dzielnicy Centrum .
Jej początki wiążą się z budowę huty Laura ( obecnie zlikwidowana huta Jedność ) .
Sama zaś kolonia robotnicza powstała w latach 50 .
XIX wieku , a z biegiem czasu w rejonie Hugo zaczęły powstawać kamienice mieszczańskie .
Od 1924 roku Hugo jest dzielnicą obecnych Siemianowic Śląskich .
