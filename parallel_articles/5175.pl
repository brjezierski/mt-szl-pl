Hujowa Górka
Hujowa Górka ( także : Chujowa Górka , `` Hujarowa Górka '' , `` Kozia Górka '' , `` H-Górka '' ) – miejsce na terenie byłego niemieckiego obozu koncentracyjnego Płaszów w Krakowie , w którym od września 1943 dokonywano egzekucji więźniów obozu oraz więźniów z Montelupich i aresztów .
Wśród ofiar zdecydowanie przeważali Żydzi ; incydentalnie rozstrzeliwano tam również Polaków i przedstawicieli innych narodowości .
Największe nasilenie egzekucji miało miejsce wiosną 1944 .
Hujowa Górka była położona na niewielkim wzniesieniu tzw .
Górek Płaszowskich , w tym miejscu znajdował się dawny szaniec polowy FS-21 wzniesiony przez Austriaków w II połowie XIX wieku , gdy wokół Krakowa powstawał system fortyfikacji .
Używany był w okresie I wojny światowej .
Sama Górka , jako miejsce egzekucji , była właściwie dołem , o wymiarach 6 m szerokości , 50 m obwodu i głębokości 5 m .
Egzekucje .
Kraków został zajęty przez Wehrmacht 6 września 1939 , a już 10 września 1939 w tym miejscu Niemcy dokonali pierwszej zbiorowej egzekucji Polaków .
Pod murem Podgórskiego nowego cmentarza żydowskiego rozstrzelanych zostało wtedy 13 krakowian .
Zabito 11 znanych z nazwiska mężczyzn oraz 2 nieznanych .
Dla upamiętnienia powstał w tym miejscu `` Pomnik Martyrologii '' .
Kolejne egzekucje – transport Żydów z getta w Bochni – odbyły się pod koniec sierpnia lub na początku września 1943 .
Oprócz więźniów płaszowskiego obozu oraz więźniów budynku przy Montelupich , zabijano tu także Żydów używających fałszywych dokumentów , poświadczających aryjskie pochodzenie .
Egzekucje przeprowadzano niemalże codziennie do połowy lutego 1944 .
Ofiary rozstrzeliwano grupami , były one uprzednio zmuszane do rozebrania się .
Ciała układano warstwami , naprzemiennie ( głowa-nogi ) , po czym zasypywano warstwą ziemi i kamieni .
