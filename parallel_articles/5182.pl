Huntington Beach
Huntington Beach , miasto w Stanach Zjednoczonych , w hrabstwie Orange w południowo-zachodniej części stanu Kalifornia , nad Oceanem Spokojnym .
Ma 199 223 mieszkańców .
Huntington Beach pełni głównie funkcję mieszkaniową pobliskich aglomeracji .
Jest miejscem zamieszkiwanym przez wiele gwiazd i skateboardzistów .
To także ośrodek turystyczno-wypoczynkowy .
Posiada liczne kąpieliska morskie i długie plaże , które oferują wiele możliwości do surfowania .
W mieście rozwinął się przemysł lotniczy oraz rafineryjny .
