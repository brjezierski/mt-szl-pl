Huntsville ( Kanada )
Huntsville – miejscowość ( ang .
`` town '' ) w Kanadzie , w prowincji Ontario , w dystrykcie Muskoka .
Liczba mieszkańców Huntsville wynosi 18 280 .
Język angielski jest językiem ojczystym dla 92,9 % , francuski dla 1,7 % mieszkańców ( 2006 ) .
W Huntsville urodziła się Dara Howell , kanadyjska narciarka dowolna , specjalizująca się w slopestyle ' u i halfpipe'ie .
