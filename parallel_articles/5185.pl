Język chunzybski
Język chunzybski ( także : język chunzalski , język nachadzki ; ros .
гунзибский , гунзельский , хунзальский язык , także : нахадский язык ; nazwa własna `` Hunzibi '' ) – jeden z niewielkich języków kaukaskich , używany przez ok. 1 tys .
osób w południowym Dagestanie .
Język chunzybski należy do języków didojskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Chunzybski jest blisko spokrewniony z językiem kapuczyńskim .
Istnieją spory odnośnie do statusu chunzybskiego w ramach języków didojskich .
Niektórzy badacze uważają go za odrębny język , inni zaś tylko za dialekt .
Dane , mówiące o liczbie osób posługujących się tym językiem nie są zbyt dokładne , ponieważ w dużej mierze opierały się na szacunkach .
