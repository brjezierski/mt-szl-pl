– miasto we wschodnim Egipcie , na wybrzeżu Morza Czerwonego , główny ośrodek administracyjny Prowincji Morza Czerwonego – jednej z 27 egipskich muhafaz ( prowincji gubernatorskich ) , jeden z największych w Egipcie ośrodków turystycznych o znaczeniu międzynarodowym ( Riwiera Morza Czerwonego ) .
Nazwa miasta pochodzi od czerwonych jagód krzewu ( `` الغرقد أو الغردق '' ) z rodzaju łużnik ( `` Nitraria '' ) , z którego Beduini robią orzeźwiający napój .
Historia .
Hurghada powstała nad Zatoką Sueską z brytyjskiej osady poszukiwaczy ropy naftowej ( od ok. 1913 r. ) i początkowo odgrywała rolę osady rybackiej .
W 1930 r. powstała osada mieszkaniowa dla pracowników `` Narodowego Instytutu Oceanografii i Rybołówstwa '' .
Od wczesnych lat 80 .
XX w. po pokoju egipsko-izraelskim podpisanym w Camp David zaczęła się rozwijać jako międzynarodowy ośrodek turystyczny dzięki krajowym i zagranicznym inwestorom .
14 czerwca 2010 w okolicach miasta , prawdopodobnie z tankowca , nastąpił wyciek ropy .
