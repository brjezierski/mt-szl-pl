Hustopeče
Hustopeče ( niem .
`` Auspitz '' ) − miasto na Morawach , w Czechach , w kraju południowomorawskim , w powiecie Brzecław .
Według danych z 31 grudnia 2003 powierzchnia miasta wynosiła 2 453 ha , a liczba jego mieszkańców 5 913 osób .
Miasto Hustopeče położone jest w południowo-wschodniej części Czech , w pobliżu granicy ze Słowacją .
W pobliżu miejscowości znajduje się węzeł na autostradzie D2 .
