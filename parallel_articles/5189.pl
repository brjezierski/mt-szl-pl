Huta Małapanew
Huta Małapanew – huta żelaza położona w Ozimku nad rzeką Mała Panew .
Jest to najstarsza huta działająca w Polsce .
Została założona w 1754 roku .
Obecnym właścicielem jest Gwarant Grupa Kapitałowa w Katowicach .
Historia .
Decyzję o budowie huty podjął król pruski Fryderyk II .
Hutę zlokalizowano na lewym brzegu rzeki Mała Panew , między miejscowościami Krasiejów i Schodnia .
Już w 1754 zaczął pracować pierwszy wielki piec .
Od roku 1781 w Pruskiej Królewskiej Hucie Malapane pracował Ephraim Ludwig Gottfried Abt , początkowo wymieniany jako mistrz ( niem .
`` Bergmeister '' ) , a potem jako jej zarządca ( `` Oberhütteninspektor '' ) .
Jego zadaniem było między innymi nadzorowanie prób wytopu żelaza z użyciem węgla kamiennego .
Za jego zarządu w hucie , którą sam określał jako „ matkę śląskich hut ” , w 1789 r. przeprowadzono pierwsze udane próby użycia koksu do wytopu żelaza .
W 1795 r. powstał w zakładzie pierwszy w Europie kontynentalnej metalowy most drogowy , zamontowany rok później na Strzegomce w miejscowości Łażany ( zniszczony w roku 1945 ) .
Kolejnym ważnym wydarzeniem w dziejach ozimskiej huty było wybudowanie w 1827 roku żeliwnego mostu wiszącego na Małej Panwi , stanowiącego obecnie zabytek , który jest wpisany do rejestru zabytków .
W latach 70 .
XX w. zatrudnienie w hucie wynosiło 7 tysięcy pracowników .
W 1958 przy Hucie Małapanew , Zakładach Przemysłu Bawełnianego „ Frotex ” w Prudniku i Zakładach Przemysłu Azotowego „ Kędzierzyn ” , powstały pierwsze w województwie kluby honorowych dawców krwi .
