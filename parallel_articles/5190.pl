Huta Padniewska
Huta Padniewska – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie mogileńskim , w gminie Mogilno .
Miejscowość wchodzi w skład sołectwa Padniewo .
Historia .
Na terenie wsi znajduje się stanowisko archeologiczne – fragmenty osady wczesnośredniowiecznej .
Pierwsze pisane wzmianki o miejscowości pochodzą z 1730 roku .
W księgach kościelnych fary mogileńskiej wymienia się Hutę Padniewską jako miejsce , gdzie osiedlała się ludność żydowska , której zakazano osiedlania się w Mogilnie .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 20 mieszkańców .
Jest 52. co do wielkości miejscowością gminy Mogilno .
Eksploatacja kopalin .
W okolicy eksploatowane są złoża żwiru i piasku .
Likwidacja sołectwa .
Podczas wyborów sołtysów i członków rad sołeckich w 2007 roku mieszkańcy Huty Padniewskiej i Przyjmy odmówili dokonania wyboru sołtysa i wystąpili do burmistrza Mogilna z wnioskiem o likwidację sołectwa .
27 czerwca 2007 Rada Gminy Mogilno przychyliła się do tego wniosku .
Hutę Padniewską przyłączono do sołectwa Padniewo , natomiast Przyjmę do sołectwa Palędzie Dolne
