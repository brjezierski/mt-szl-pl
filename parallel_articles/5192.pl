Huta-Dąbrowa
Huta-Dąbrowa – wieś w Polsce położona w województwie lubelskim , w powiecie łukowskim , w gminie Krzywda .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Nazwa .
Nazwa miejscowości została nadana od huty szkła , zaś drugi człon nazwy , „ Dąbrowa ” , pochodzi prawdopodobnie od lasów dębowych , które tu niegdyś dominowały .
Miejscowi często zamiast pełnej nazwy używają jedynie pierwszego członu – „ Huta ” .
Huta szkła .
Charakterystycznym obiektem w miejscowości jest huta szkła .
Huta powstała w 1824 roku i działa po dziś dzień .
Pierwszy właściciel zakupił kilkadziesiąt hektarów lasów dębowych od dziedzica majątku Radoryż Smolany .
Huta początkowo nosiła nazwę „ Jarczew ” , bo była zbudowana na terenie gminy Jarczew , a dopiero później nazywano hutę „ Dąbrową ” ; nosiła też nazwę „ Ciemnej Dąbrowy ” .
Początkowo zabudowania huty były bardzo prymitywne .
