Hutthurm
Hutthurm – gmina targowa w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Bawaria , w regionie Donau-Wald , w powiecie Pasawa .
Leży około 12 km na północ od Pasawy , przy drodze B12 i linii kolejowej Pasawa – Freyung .
Podział .
W skład gminy wchodzą cztery części miejscowości : Hutthurm , München , Prag , Leoprechting .
Oświata .
W gminie znajduje się 150 miejsc przedszkolnych ( 177 dzieci ) oraz szkoła podstawowa ( 29 nauczycieli , 533 uczniów ) .
