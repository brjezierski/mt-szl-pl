Huế
Huế − miasto w środkowym Wietnamie , nad rzeką Hương , 11 km od jej ujścia do Morza Południowochińskiego , ośrodek administracyjny prowincji Thừa Thiên-Huế .
W 2009 roku liczyło 302 983 mieszkańców .
Do XVII w. nazywane Phú Xuân .
Siedziba rzymskokatolickiej archidiecezji Huế .
Pod koniec XVI w. Wietnam , nominalnie rządzony przez dynastię Lê , został podzielony między dwa rody magnackie : Trịnh i Nguyễnów .
Nguyễnom przypadło południe Wietnamu , dawne ziemie Czamów , zajęte wówczas do przełęczy Cù Mông .
W 1687 r. , Nguyễnowie założyli nową siedzibę , stolicę domeny , w miejscowości Phú Xuân , leżącej nad rzeką Perfumową , kilkanaście kilometrów od ujścia .
W pobliżu miasta wybudowano ogromną cytadelę , na terenie której dwór Nguyễnów rezydował do 1775 r. Na przełomie 1774 i 1775 r. Trịnhowie wykorzystując zamieszanie spowodowane powstaniem Tajsonów niespodziewanym atakiem przekroczyli , rozdzielający domeny , mur Đồng Hới .
Dwór Nguyễnów w panice porzucił Phú Xuân i przeniósł się do Gia Định .
Miasto znalazło się pod panowaniem Tajsonów .
W początkach grudnia 1788 r. średni z braci Nguyễn Huệ ogłosił się cesarzem północy .
