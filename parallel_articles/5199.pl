Hvar ( miasto )
Hvar ( wł .
`` Lesina '' ) – miasto w Chorwacji , w żupanii splicko-dalmatyńskiej , siedziba miasta Hvar .
Jest położony na dalmatyńskiej na wyspie o tej samej nazwie .
W 2011 roku liczył 3771 mieszkańców .
W mieście znajdują się mury z VII w. , najstarszy teatr miejski w Europie otwarty w 1612 roku , muzea , galerie i wystawy .
Hvar jest popularnym kurortem turystycznym .
W centrum miasteczka znajduje się niewielki port jachtowy , otoczony zabytkową zabudową , a także żwirowe i skaliste plaże .
Na przedmieściach miasta znaleźć można urokliwe zatoczki .
Kurort znany jest z dużej ilości restauracji , barów i dyskotek .
Koronki z Hvaru .
Na wyspie Hvar istniała tradycja wykonywania z włókna agawy lin i nici do wyplatania sieci rybackich .
Od XIX wieku koronki teneryfowa z nici agawy wykonują je wyłącznie zakonnice z miejscowego klasztoru benedyktynek .
Koronkarstwo zostało wpisane na listę niematerialnego dziedzictwa UNESCO w Europie .
