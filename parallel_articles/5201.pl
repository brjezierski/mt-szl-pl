Hyaloscypha aureliella ( Nyl . )
Huhtinen – gatunek grzybów należący do rodziny przeźroczkowatych ( `` Hyaloscyphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hyaloscypha , Hyaloscyphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1866 roku William Nylander , nadając mu nazwę `` Peziza aureliella '' .
Obecną nazwę nadał mu w 1990 r. Seppo Huhtinen .
