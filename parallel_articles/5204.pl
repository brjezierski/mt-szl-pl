Hydropulper
Hydropulper – maszyna wykorzystywana między innymi w produkcji papieru toaletowego , służąca do rozdrabniania i rozwłókniania w dużej ilości wody masy celulozowej , makulatury , szmat oraz opakowań do żywności płynnej ( tzw .
TetraPak ) itp .
Hydropulper zalicza się do tzw mokrej części maszyny papierniczej , służy do wirowego rozwłókniania celulozy , makulatury i nadaje się doskonale na niszczenie dokumentów .
