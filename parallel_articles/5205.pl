Mokronóżka czerniejąca
Mokronóżka czerniejąca ( `` Hydropus atramentosus '' ( Kalchbr . )
Kotl .
& amp ; Pouzar ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hydropus , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował Károly Kalchbrenner w 1873 r. nadając mu nazwę `` Agaricus atramaentosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu František Kotlaba i Zdeněk Pouzar w 1962 r .
