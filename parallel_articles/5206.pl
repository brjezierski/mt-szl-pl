Stożkownica czapeczkowata
Stożkownica czapeczkowata , wilgotnica czapeczkowata ( `` Porpolomopsis calyptriformis '' ( Berk . )
Bresinsky ) – gatunek grzybów z rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Porpolomopsis , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1838 r. Miles Joseph Berkeley nadając mu nazwę `` Agaricus calyptriformis '' , później zaliczany był do różnych innych rodzajów .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 2008 r. Andreas Bresinsky przenosząc go do rodzaju `` Porpolomopsis '' .
Synonimy naukowe :
Barbara Gumińska i Władysław Wojewoda w 1983 r. podali polską nazwę wilgotnica czapeczkowata .
Od 2012 stała się ona niespójna z nazwą naukową , gatunek ten bowiem przeniesiony został do rodzaju `` Humidicutis '' , a po tem do rodzaju `` Porpolomopsis '' .
W 2021 Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała nazwę stożkownica czapeczkowata .
Morfologia .
Średnica od 3 do 7 cm , młody – ostro stożkowaty , w kształcie czapki z grotem , z brzegiem przyrośniętym do trzonu , później stożkowaty do rozpostartego , ale pośrodku ciągle spiczasty .
Brzegi pofałdowane i podwinięte .
Powierzchnia gładka , matowa , promieniście pokryta delikatnymi włókienkami .
Jest kruchy i łatwo chłonie wodę , u dojrzałych owocników promieniście popękany .
Barwa różowa , różowoczerwona , liliowa , u dojrzałych grzybów do wierzchołka blednie .
