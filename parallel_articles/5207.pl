Lisówka wielkozarodnikowa
Lisówka wielkozarodnikowa ( `` Hygrophoropsis macrospora '' ( D.A .
Reid ) Kuyper ) – gatunek grzybów z rodziny lisówkowatych ( `` Hygrophoropsidaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hygrophoropsis , Hygrophoropsidaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1972 roku Derek Agutter Reid , jako odmianę lisówki pomarańczowej ( `` Hygrophoropsis aurantiaca '' var .
`` macrospora '' ) .
W 1996 r. Thomas Wilhelmus Kuyper podniósł ją do rangi odrębnego gatunku .
Synonim : `` Merulius aurantiacus '' var .
`` lacteus '' Harzer 1842 .
W niektórych atlasach grzybów ma nazwę lisówka wielkozarodnikowa będacą tłumaczeniem nazwy naukowej .
Morfologia .
Średnica od 2 do 4 cm , u młodych okazów lekko wypukły , potem płaski , u starszych wklęsły .
Powierzchnia drobno owłosiona , kremowobiaława do bladoochrowej , ochrowożółtawa .
Brzeg falisty , stale podwinięty .
