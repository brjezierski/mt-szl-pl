Lisówka blada
Lisówka blada ( `` Hygrophorus pallidus '' Peck ) – gatunek grzybów z rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hygrophorus , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1902 roku Charles Horton Peck i nadana przez niego nazwa naukowa jest aktualna .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. dla nazwy naukowej `` Hygrophoropsis pallida '' ( Cooke ) Kreisel .
Jest niespójna z aktualną nazwą naukową .
