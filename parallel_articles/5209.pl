Wodnicha modrzewiowa
Wodnicha modrzewiowa ( `` Hygrophorus lucorum '' Kalchbr . )
– gatunek grzybów owocnikowych należący do rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hygrophorus , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Niektóre synonimy naukowe :
Nazwę polską podała Barbara Gumińska w 1997 r .
Morfologia .
Średnicy 2–6 cm , u młodych okazów wypukły , u starszych spłaszczony , a nawet czasami wgłębiony na środku .
Powierzchnia gładka i lepka , kolor cytrynowożółty lub złocisty .
Nieco zbiegające , dość rzadkie i dość grube .
Początkowo są białe , u starszych okazów żółkną .
Wysokość 4–6 cm , grubość do 1 cm , walcowaty , miękki i lepiący się .
Jest jaśniejszy od kapelusza i ma kolor blado cytrynowożółty .
Żółtawy , nie zmieniający barwy po uszkodzeniu .
Smak i zapach łagodny .
Biały .
Zarodniki wydłużone , gładkie , o rozmiarach 7–91 × 4–5 μm .
Występowanie i siedlisko .
W Polsce jest dość częsta , w górach bardziej , niż na niżu .
Grzyb mikoryzowy , rośnie wyłącznie pod modrzewiami .
Owocniki pojawiają się jesienią , aż do silniejszych mrozów , czasami , również w zimie .
Znaczenie .
Grzyb jadalny , w Polsce jednak zazwyczaj nie jest zbierany .
Jest to jednak dobry grzyb jadalny .
