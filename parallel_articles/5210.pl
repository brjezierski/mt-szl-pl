Wodnicha pomarańczowa
Wodnicha pomarańczowa ( `` Hygrophorus pudorinus '' ( Fr . )
Fr . )
– gatunek grzybów z rodziny wodnichowatych ( `` Hygrophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hygrophorus , Hygrophoraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1821 Elias Fries nadając mu nazwę `` Agaricus pudorinus '' .
W 1836 r. ten sam autor nadał mu obecną , uznaną przez Index Fungorum nazwę , przenosząc go do rodzaju `` Hygrophorus '' .
Niektóre synonimy nazwy naukowej :
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda w 1983 .
Morfologia .
Średnica od 5 do 15 cm , początkowo półkulisty , później wypukły , u dojrzałych owocników płasko wypukły .
