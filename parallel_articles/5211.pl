Szczeciniak jaworowy
Szczeciniak jaworowy , szczecinkowiec jaworowy ( `` Hymenochaete carpatica '' Pilát ) – gatunek grzybów z rzędu szczeciniakowców ( `` Hymenochaetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenochaete , Hymenochaetaceae , Hymenochaetales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten w 1930 r. opisał Albert Pilát na korze klonu jawora na Ukrainie .
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Rozpostarty , płaski , pokryty kolcami .
Powierzchnia jasnobrązowa .
Starsze owocniki stają się ciemniejsze , niemal czarnobrązowe i przerastają korę .
Z wiekiem staje się także coraz bardziej twardy i popękany .
Brzeg nieco włóknisty .
