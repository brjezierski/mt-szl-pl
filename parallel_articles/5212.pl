Szczeciniak jodłowy
Szczeciniak jodłowy , szczecinkowiec jodłowy ( `` Hymenochaete cruenta '' ( Pers . )
Donk ) – gatunek grzybów z rodziny szczeciniakowatych ( `` Hymenochaetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenochaete , Hymenochaetaceae , Hymenochaetales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1801 Christian Hendrik Persoon nadając mu nazwę `` Thelephora cruenta '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1959 Marinus Anton Donk , przenosząc go do rodzaju `` Hymenochaete '' .
Synonimy naukowe :
W 1968 r. Barbara Gumińska i Władysław Wojewoda podali nazwę szczecinkowiec jodłowy , w 1999 r. Władysław Wojewoda zmienił ją na szczeciniak jodłowy .
Morfologia .
Rozpostarty płasko , skorupiasty , bardzo cienki ( tylko do 4 mm grubości ) , owalny lub kolisty , o brzegu falistym , karbowanym i odgiętym .
Powierzchnia górna o barwie jasnoczerwonej lub czerwonej , po obumarciu staje się brązowoczerwona .
