Hymenoscyphus caudatus
Hymenoscyphus caudatus ( P .
Karst . )
Dennis – gatunek grzybów z rodziny tocznikowatych ( `` Helotiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenoscyphus , Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1866 r. Petter Adolf Karsten nadając mu nazwę `` Peziza cauidata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1964 r. Richard William George Dennis .
