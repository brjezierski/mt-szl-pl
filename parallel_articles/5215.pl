Hymenoscyphus immutabilis
Hymenoscyphus immutabilis ( Fuckel ) Dennis – gatunek grzybów z rodziny tocznikowatych ( `` Helotiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenoscyphus , Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1871 r. Leopold Fuckel nadając mu nazwę `` Helotium immutabile '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1964 r. Richard William George Dennis .
Synonimy :
Morfologia .
Apotecja siedzące lub krótkotrzonowe , występujące pojedynczo lub skupiskami na opadłych liściach , zwłaszcza na ich nerwach .
