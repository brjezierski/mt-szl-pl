Hymenoscyphus kathiae
Hymenoscyphus kathiae ( Korf ) Baral – gatunek grzybów z rodziny tocznikowatych ( `` Helotiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenoscyphus , Helotiaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1999 r. Richard Paul Korf nadając mu nazwę `` Pezoloma kathiae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 2005 r. Hans-Otto Baral .
Morfologia .
Apotecja złożone z miseczki i trzonu o łącznej wysokości do 1,5 mm .
Miseczka o średnicy 1,2–1,5 mm .
Powierzchnia wewnętrzna miseczki początkowo w barwie kości słoniowej , potem stopniowo ciemniejąca – szarożółta do musztardowożółtej .
Sterylna powierzchnia zewnętrzna w ekscypulum tej samej barwy , u podstawy czarniawo punktowana .
Trzon wełnisty , pokryty galaretowatą warstewką dochodzącą po zewnętrznej stronie miseczki mniej więcej do połowy jej wysokości i tworzącą zewnętrzną warstwę ekscypulum .
