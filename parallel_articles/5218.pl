Hymn Chile
Hymn Chile został przyjęty w roku 1847 .
Słowa napisał Eusebio Lillo Robles , a muzykę skomponował Ramón Carnicer y Battle .
Historia .
Pierwszy hymn Chile , zaakceptowany przez rząd w roku 1819 , był skomponowany przez Manuela Roblesa , a słowa napisał Bernardo de Vera y Pintado .
Został wykonany po raz pierwszy 20 sierpnia 1820 roku na przyjęciu z okazji urodzin Bernardo O ’ Higginsa .
W roku 1828 rząd poprosił Ramóna Carnicera o stworzenie nowej muzyki , bazując na tekście de Very .
Następnie , w 1847 roku , Eusebio Lillo Robles został wyznaczony do napisania tekstu , który miał zastąpić stare słowa zawierające odniesienia nawołujące do przemocy przeciwko Hiszpanii .
Nowy hymn zachował refren poprzedniej pieśni .
Jako oficjalny hymn państwowy używany jest tylko refren i piąta zwrotka .
Za czasów Augusto Pinocheta w latach 1973-1990 junta wojskowa nakazała , że jako oficjalna ma być używana także trzecia zwrotka oryginalnego tekstu , odnosząca się do sił zbrojnych Chile , jako druga oficjalna .
Po roku 1990 powrócono do stanu sprzed zamachu stanu w 1973 usuwając militarną zwrotkę .
