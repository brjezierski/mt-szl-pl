Hypatia z Aleksandrii
Hypatia z Aleksandrii ( , ur .
ok. 355 lub 370 , zm .
w marcu 415 w Aleksandrii ) – aleksandryjska filozofka neoplatońska , matematyczka ; córka Teona z Aleksandrii .
Zwana również „ męczennicą nauki ” .
Legenda przypisuje Hypatii wynalezienie astrolabium i rodzaju areometru .
Życiorys .
Córka matematyka , astronoma i filozofa Teona z Aleksandrii , wykształcenie matematyczne odebrała najpewniej pod jego kierunkiem .
Być może kształciła się w Atenach .
Jednak prawdopodobnie nigdy nie opuściła rodzinnego miasta .
Była aktywna na polu nauki i filozofii .
Nie zachowały się żadne jej pisma , ale można jej hipotetycznie przypisać współpracę z ojcem przy redakcji pism Ptolemeusza i Euklidesa .
Wiadomo , że pracowała nad komentarzami i wydaniem traktatów „ Arytmetyka ” Diofantosa i „ Stożkowe ” Apoloniusza z Pergi .
Nie można wykluczyć , że ostateczny kształt tych dzieł , jaki dotarł do naszych czasów , jak również „ Almagestu ” i „ Tablic podręcznych ” Ptolemeusza , jest dziełem Hypatii .
Nieco więcej wiadomo o jej działalności na polu filozofii , szczególnie dzięki listom jej ucznia Synezjusza z Cyreny , biskupa Ptolemaidy .
Utworzyła wokół siebie krąg oddanych uczniów , wspólnotę dążącą do wiedzy i poznania natury rzeczywistości , opierając się na filozofii pitagorejsko-platońskiej .
Hypatia wymagała od siebie i swych uczniów wysiłku umysłu , woli i wyrzeczeń .
Jednocześnie jej postawa była tolerancyjna i odznaczała się otwartym spojrzeniem na problemy filozoficzne .
Wśród jej uczniów byli chrześcijanie i poganie , pochodzący nie tylko z Aleksandrii i Egiptu , ale z Syrii , Cylicji i Konstantynopola .
Jej uczniowie odpłacali jej przywiązaniem i miłością długo po osiągnięciu samodzielności , a nawet wysokich godności kościelnych .
Choć filozoficzny krąg jej uczniów miał charakter elitarny , Hypatia wykładała również publicznie matematykę i astronomię .
Wykłady prowadziła w swym domu , przed którym zbierały się tłumy entuzjastów , i w miejskich salach wykładowych .
Często jeździła powozem , zatrzymywała się po drodze w instytucjach publicznych , rozmawiała z urzędnikami i dostojnikami .
Była niezwykle szanowana wśród swoich zwolenników i uchodziła za autorytet moralny .
Ceniono jej wiedzę , logikę i inne zalety intelektualne oraz cechy jej charakteru : umiar , dystynkcję w obejściu , prostotę ubioru , wstrzemięźliwość seksualną ( do końca życia pozostała dziewicą ) , miłość do Aleksandrii , w której sprawy się angażowała .
Hypatia a chrześcijaństwo .
Hypatia nie była praktykującą poganką .
Nie chcąc dostarczać pretekstu chrześcijańskiej administracji miasta do ataku na siebie i swoich uczniów , respektowała prawodawstwo antypogańskie , nie uczęszczała do świątyń , nie uczestniczyła w protestach przeciwko burzeniu świątyń pogańskich lub zamienianiu ich na chrześcijańskie kościoły .
Nic nie wiadomo na temat jej postawy podczas burzenia Serapejonu w 391 roku .
Nie czyniła różnicy między swymi pogańskimi i chrześcijańskimi uczniami .
Opiekowała się wszystkimi studentami , niezależnie od ich światopoglądu .
Utrzymywała kontakty z urzędnikami cesarskimi i miejskimi , z których większość była chrześcijanami .
Był nim również Orestes , prefekt cesarski .
Dzięki temu cieszyła się powszechnym szacunkiem .
Za czasów biskupa Teofila , odpowiedzialnego za zniszczenie Serapejonu , nie spotykały jej żadne szykany .
Śmierć .
Sytuacja zmieniła się po objęciu biskupstwa w 412 roku przez bratanka Teofila , Cyryla ( późniejszego świętego Cyryla z Aleksandrii ) , który szybko popadł w konflikt z cesarskim prefektem i namiestnikiem Egiptu Orestesem , przyjacielem Hypatii .
Cyryl i inni księża zaczęli atakować aleksandryjską filozofkę i matematyczkę , oskarżając ją z ambon aleksandryjskich kościołów o czary i uprawianie satanizmu .
Jednak przyczyny religijno-polityczne były tylko jednym z powodów ataku Cyryla na Hypatię .
Cyryl zazdrościł aleksandryjskiej matematyczce popularności wśród mieszkańców Aleksandrii .
