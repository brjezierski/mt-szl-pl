Strzępkoskórka kremowa
Strzępkoskórka kremowa ( `` Hyphoderma litschaueri '' ( Burt ) J. Erikss .
& amp ; Å. Strid ) – gatunek grzybów z rzędu szczeciniakowców ( `` Hymenochaetales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hyphoderma , Hyphodermataceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten opisał w 1926 roku Edward Angus Burt , nadając mu nazwę `` Corticium litschaueri '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu 1975 r. John Eriksson i Åke Strid w 1975 r .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Rozpostarty o grubości do 170 µm .
Powierzchnia hymenialna gładka lub grudkowata , na świeżych owocnikach szarobiała , na wyschniętych pomarańczowobiała do jasnopomarańczowej .
