Maślanka wrzosowa
Maślanka wrzosowa , łysiczka wrzosowa ( `` Hypholoma ericaeum '' ( Pers . )
Kühner ) – gatunek grzybów z rodziny pierścieniakowatych ( `` Strophariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hypholoma , Strophariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Agaricus ericaeus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1936 r. Robert Kühner przenosząc go do rodzaju `` Hypholoma '' .
Synonimy :
Nazwę maślanka wrzosowa podał Władysław Wojewoda w 1999 r. Inne nazwy polskie : bedłka wrzosowa ( Feliks Berdau 1876 ) , kołpaczek wrzosowy ( Franciszek Błoński 1889 ) , cierniówka wrzosowa ( Stanisław Chełchowski 1898 ) , łysak wrzosowiskowy ( Józef Teodorowicz 1936 ) .
