Podgrzybnica gołąbkowa
Podgrzybnica gołąbkowa ( `` Hypomyces lactifluorum '' ( Schwein . )
Tul . )
– gatunek grzyba z rodziny `` Hypocreaceae '' .
Pasożytniczy grzyb workowy , atakujący grzyby z rodziny gołąbkowatych .
W angielskim obszarze językowym spasożytowane przez niego owocniki z powodu swojego koloru podobnego do ugotowanego homara są też nazywane `` '' .
W Ameryce Północnej jest to grzyb handlowy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hypomyces , Hypocreaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1822 r. Lewis David von Schweinitz nadając mu nazwę `` Sphaeria lactifluorum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1860 r. Louis René Tulasne i Charles Tulasne .
Synonimy :
Nazwę polską zarekomendowała w 2021 r. Komisja ds .
Nazewnictwa Grzybów przy Polskim Towarzystwie Mykologicznym .
Cechy .
Podgrzybnica gołąbkowa pokrywa swoją grzybnią owocniki gołąbków i mleczajów , tworząc na nich twardą , podobną do pleśni warstwę .
Jej strzępki nadają im barwę od jasnopomarańczowej do czerwono-fioletowej .
Miąższ grzyba pozostaje biały .
