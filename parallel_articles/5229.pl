Podgrzybnica drobnozarodnikowa
Podgrzybnica drobnozarodnikowa ( `` Hypomyces microspermus '' Rogerson & amp ; Samuels ) – gatunek grzybów należący do rodziny rozetkowatych ( `` Hypocreaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hypomyces , Hypocreaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1832 r. Clark Thomas Rogerson , nadając mu nazwę `` Sepedonium chrysospermum '' .
Obecną nazwę nadał mu w 1989 r. Gary Joseph Samuels .
Nazwa polska na podstawie rekomendacji Komisji ds .
Polskiego Nazewnictwa Grzybów .
Morfologia .
