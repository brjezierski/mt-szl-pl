Hypomyces ochraceus
Hypomyces ochraceus ( Pers . )
Tul .
& amp ; C. Tul .
– gatunek grzybów należący do rodziny rozetkowatych ( `` Hypocreaceae '' ) .
Pasożyt wielu gatunków grzybów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hypomyces , Hypocreaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Sphaeria ochracea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1865 r. Louis René Tulasne i Charles Tulasne .
Synonimy :
Morfologia .
Na powierzchni żywiciela tworzy płaską , początkowo białą , potem czerwonawo-ochrową podkładkę z kulistymi owocnikami typu perytecjum o stożkowym ostiolum i delikatnym , żółtawym perydium .
Worki 8-zarodnikowe , wrzecionowate , 250–300 × 6,5 µm .
Zarodniki w workach częściowo zachodzą na siebie , są długie , wrzecionowate z ostro zakończonymi wierzchołkami , 35 × 6,5 µm , szkliste , w stanie dojrzałym dwukomórkowe i zwężone na przegrodzie .
Tworzy dwie bezpłciowe morfy , dawniej opisywane jako odrębne gatunki : `` Verticillium agaricinum '' i `` Blastotrichium puccinioides '' .
W Polsce obserwowano wyłącznie te formy .
Podzielone przegrodami i rozgałęzione konidiofory o długości 55-88 µm powstają pęczkami po kilka .
