Drewniak jesionowy
Drewniak jesionowy ( `` Hypoxylon fraxinophilum '' Pouzar ) – gatunek grzybów z rzędu próchnilcowców ( `` Xylariales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hypoxylon , Hypoxylaceae , Xylariales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1972 r. Zdeněk Pouzar na leżących na ziemi gałęziach jesiona wąskolistnego ( `` Fraxinus angustifolia '' ) w Czechosłowacji .
