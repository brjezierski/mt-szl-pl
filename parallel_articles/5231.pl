Morfologia .
W górnej warstwie kory drzew tworzy półkoliste , nieco zwężone u podstawy podkładki o średnicy 2–6 mm .
Mają barwę od różowawo brązowej do szarobrązowej , wewnątrz w stanie dojrzałym ciemnoszarobrązową .
Owocniki w pojedynczej warstwie , nieco podnoszącej powierzchnię podkładki i z widocznymi , lekko zapadniętymi ostiolami otoczonymi wypukłym , białym pierścieniem niepigmentowanych strzępek .
Czarne owocniki mają średnicę 350–450 µm i wysokość 400–650 µm , kształt od kulistego do jajowatego , ale gdy rosną w skupiskach są ściśnięte .
