Hå
Gmina Hå ( ) – norweska gmina leżąca w regionie Rogaland .
Jej siedzibą jest miasto Varhaug .
Hå jest 299. norweską gminą pod względem powierzchni .
Demografia .
Według danych z roku 2005 gminę zamieszkuje 14 784 osób , gęstość zaludnienia wynosi 58 os./km² .
Pod względem zaludnienia Hå zajmuje 65. miejsce wśród norweskich gmin .
