Hösbach
Hösbach – gmina targowa w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Frankonia , w regionie Bayerischer Untermain , w powiecie Aschaffenburg .
Leży około 6 km na północny wschód od Aschaffenburga , przy autostradzie A3 , drodze B26 i linii kolejowej Frankfurt nad Menem – Würzburg .
