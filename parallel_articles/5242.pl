Honduras
Honduras , Republika Hondurasu ( hiszp .
`` Honduras '' , `` República de Honduras '' ) – państwo w Ameryce Północnej , pomiędzy Morzem Karaibskim a Oceanem Spokojnym ( zatoka Fonseca ) , o łącznej długości wybrzeża 820 km .
Honduras graniczy na zachodzie z Gwatemalą ( dł .
granicy 256 km ) , na południowym wschodzie z Nikaraguą ( 922 km ) i na południowym zachodzie z Salwadorem ( 342 km ) .
Do Hondurasu należą również przybrzeżne wyspy , z których największe to Islas de la Bahía .
Od 1972 roku do Hondurasu należą Islas Santanilla .
Geografia .
Honduras , podobnie jak inne państwa leżące w Ameryce Środkowej , to kraj wyżynno-górzysty .
Tereny górzyste zajmują prawie 4/5 powierzchni kraju i ciągną się od wybrzeża Zatoki Honduraskiej nad Morzem Karaibskim po zatokę Fonseca nad Pacyfikiem .
Wąski pas równin u wybrzeża Pacyfiku ma zaledwie 70 km długości .
Wewnątrz państwa ciągnie się kilka pasm górskich pochodzenia wulkanicznego , z których najwyższy jest położony w zachodniej części grzbiet ze szczytem Cerro Las Minas ( 2890 m n.p.m .
) .
Klimat .
Na wybrzeżu Pacyfiku panuje klimat równikowy wilgotny o suchych zimach .
Średnia temperatura wynosi 28 °C , a średnia suma opadów na wyżynach waha się między 1500 a 3000 mm .
Na wiecznie wilgotnej nizinie nad Morzem Karaibskim przeciętna temperatura wynosi 25 °C .
Suma opadów jest większa na północy .
Na wybrzeżu północnym latem występują trąby powietrzne .
Nizinę nad Morzem Karaibskim ( do wysokości 1500 m n.p.m . )
porasta las tropikalny .
Na położonych wyżej obszarach w głębi kraju przeważają suche sawanny i mniej od nich rozległe wilgotne lasy górskie .
Osłonięte kotliny porasta busz i sawanna , z przewagą ciernistych krzewów .
Na równinie u wybrzeży Pacyfiku występują suche lasy , z kolei na samych wybrzeżach spotyka się namorzyny .
