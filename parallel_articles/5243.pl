Historia .
Do początku XIX wieku mała chińska wyspa Hongkong była zamieszkana przez rybaków .
Jednakże brytyjscy kupcy stworzyli z wyspy bramę dla przemycanego z Indii opium , a wywozili tędy takie towary , jak : herbata , jedwabie i porcelana , doprowadzając do poważnych konfliktów handlowych .
Uzależnienie od narkotyku stało się poważnym problemem dla Chin .
Do 1839 roku było już do ok. 10 mln palaczy opium i 2 mln narkomanów .
Władze Chin dążyły do zlikwidowania tego procederu , niszcząc przemycane opium i karząc handlarzy .
W odpowiedzi Wielka Brytania postawiła ultimatum , co doprowadziło do pierwszej wojny opiumowej ( 1839–1842 ) , która spowodowała 520 ofiar ze strony brytyjskiej i do 20 tys .
