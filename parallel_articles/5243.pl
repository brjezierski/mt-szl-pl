Etymologia .
Nazwa 香港 oznacza dosłownie „ pachnący port ” .
W języku kantońskim wymawiana jest ona jako `` hoeng1 gong2 '' ( transkrypcja jyutping ) , zaś w mandaryńskim `` Xiānggǎng '' ( transkrypcja hanyu pinyin ) .
Angielska nazwa terytorium miała pierwotnie formę `` Hongkong '' , w 1926 roku zmieniono ją na obowiązującą do dziś `` Hong Kong '' .
