II wojna światowa
II wojna światowa – największa wojna w dziejach , zaliczana do wojen totalnych , trwająca od 1 września 1939 roku do 2 września 1945 roku .
Walki toczyły się głównie w Starym Świecie ( Europie , Azji oraz Afryce ) i na wszystkich oceanach , choć miała także epizody w Arktyce i Ameryce Północnej .
Oprócz państw Europy , ich kolonii i państw Azji brały w niej udział także państwa obu Ameryk .
Głównymi stronami konfliktu były :
W wojnie uczestniczyło 1,7 mld ludzi , w tym 110 mln ludzi z bronią .
Zginęły w niej dziesiątki milionów osób ; według różnych szacunków od 50 do 78 milionów .
Rekordowe liczby to skutek między innymi ludobójstw , przede wszystkim holokaustu , pochłaniających miliony ludzkich istnień .
Wojna wybuchła 1 września 1939 roku , kiedy III Rzesza zaatakowała Polskę .
W odpowiedzi Wielka Brytania i Francja postawiły Niemcom ultimatum w sprawie bezzwłocznego wycofania wojsk z Polski .
Zostało ono zignorowane , przez co 3 września oba państwa wypowiedziały wojnę Rzeszy .
Do walk przystąpiło następnie ZSRR ; 17 września zaatakowało Polskę – zgodnie z paktem Ribbentrop-Mołotow z 23 sierpnia tego samego roku – a 28 września podpisało z Rzeszą traktat o granicach i przyjaźni .
Sowieci prowadzili również wojnę zimową przeciw Finlandii , okupowali kraje bałtyckie i zajęli część Rumunii .
Początkowo Niemcy odnosiły sukcesy , nie tylko likwidując państwo polskie , ale zajmując też Beneluks , część Skandynawii i Francji , z pozostałej tworząc kolaborancki rząd Vichy .
Przełom nastąpił 22 czerwca 1941 roku , kiedy III Rzesza zaatakowała ZSRR .
