ISO 3166-1 alfa-2
ISO 3166-1 alfa-2 – część standardu ISO 3166-1 , zawiera dwuliterowe kody państw oraz terytoriów .
Szczególnie znany z Internetowych krajowych domen najwyższego poziomu ( z kilkoma wyjątkami ) .
Na bazie tych kodów został skonstruowany standard ISO 3166-2 określający kody podziału administracyjnego każdego z państw .
