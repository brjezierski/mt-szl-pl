15 października 1915 w Różanie , zm .
30 czerwca 2012 w Tel Awiwie ) – izraelski polityk i wojskowy , premier Izraela w latach 1983–1984 i 1986–1992 , wieloletni członek Knesetu , jego przewodniczący w latach 1977–1980 , wielokrotny minister .
Stał na czele dwudziestego , dwudziestego drugiego , dwudziestego trzeciego oraz dwudziestego czwartego rządu Izraela .
Życiorys .
Młodość .
Urodził się w Różanie , w zaborze rosyjskim ( na Białorusi ) , jako Icchak Jaziernicki , syn Szlomo i Perli ( Peniny ) .
W czasach II Rzeczypospolitej był obywatelem polskim .
Ukończył szkołę hebrajską w Białymstoku i w 1929 wstąpił do młodzieżowej organizacji Bejtar .
W 1935 rozpoczął studia prawnicze w Warszawie .
Brytyjski Mandat w Palestynie .
W 1935 wyemigrował do Palestyny i rozpoczął studia na Uniwersytecie Hebrajskim w Jerozolimie , zmieniając nazwisko na Szamir .
W 1937 wstąpił do żydowskiej podziemnej organizacji wojskowej Irgun Cewai Leumi ( `` Etzel '' ) .
W 1940 , kiedy Irgun podzielił się na frakcje , Szamir przystąpił do radykalnej podziemnej organizacji wojskowej Lechi ( `` Lohamei Herut Israel '' ) , przewodzonej przez Awrahama Sterna .
Celem tego ugrupowania było utworzenie szerokiego antybrytyjskiego frontu skierowanego przeciwko okupacji Palestyny .
