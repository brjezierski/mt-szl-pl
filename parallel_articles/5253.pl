Idaho
Idaho ( ) – stan na północnym zachodzie Stanów Zjednoczonych .
Na zachodzie graniczy z Waszyngtonem i Oregonem , na południu z Nevadą i Utah , na wschodzie z Wyoming i Montaną , a na północy z Kanadą .
Obszar północnej i środkowej części stanu jest górzysty ( Góry Skaliste ) .
Na południu występują niziny ( dolina rzeki Snake ) .
