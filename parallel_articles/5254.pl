Idiomyces peyritschii
Idiomyces peyritschii Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Należy do monotypowego rodzaju `` Idiomyces '' .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Chitonomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1893 r. Roland Thaxter na odwłoku owada `` Deleaster dichrous '' w Niemczech .
Jego nazwą uczcił mykologa Johanna Josepha Peyritscha , który również zajmował się grzybami z rzędu owadorostowców .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w roku 1994 opisał jego występowanie na chrząszczu `` Deleaster dichrous '' z rodziny kusakowatych ( Staphylinidae ) .
