Igloolik
Igloolik - innuicka wioska w Nunavut w północnej Kanadzie .
Chociaż znajduje się na niewielkiej wyspie o tej samej nazwie , często bywa lokalizowana na Półwyspie Melville ’ a , z którą sąsiaduje .
Liczy 1400 mieszkańców .
Nazwa `` Igloolik '' oznacza w inuktitut tyle co „ tutaj jest igloo ” .
Merem Igloolik jest Lucassie Ivalu .
Historia .
Wykopaliska na Półwyspie Melville ’ a pokazały , iż te tereny były zamieszkane już w 2000 p.n.e .
Pierwsi Europejczycy dotarli tutaj w 1822 .
Brytyjskimi statkami dowodził kapitan William Edward Parry .
W 1867 i 1868 wyspę odwiedził amerykański podróżnik Charles Francis Hall .
W latach 30 .
XX wieku założona została w wiosce misja Kościoła rzymskokatolickiego ( obecnie 45 % mieszkańców to katolicy ) .
W wiosce działa centrum badawcze zajmujące się dokumentacją tradycji i technologii kultury innuickiej .
Prowadzone są również badania nad ruchami górotwórczymi i klimatem .
