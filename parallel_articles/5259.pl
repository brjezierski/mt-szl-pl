Ihdin
Ihdin ( ) – górskie miasto w Libanie , w kadzie Zagharta , 124 km na północ od Bejrutu .
Historia miejscowości sięga starożytności .
W VI wieku jej mieszkańcy przyjęli chrześcijaństwo w obrządku maronickim .
Mieszkańcy Ihdin wspierali krzyżowców z Hrabstwa Trypolisu w walce z muzułmanami .
W 1283 roku miejscowość została spalona przez wojska sułtana Kalawuna i ponownie zniszczona w 1586 roku .
W XVI wieku ehdeńczycy założyli Zaghartę i od tego czasu mieszkańcy obu miast tworzą jedną wspólnotę .
