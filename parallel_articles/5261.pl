Ikarus 280
Ikarus 280 – węgierski autobus przegubowy z fabryki Ikarusa .
Na początku lat 90. montowane były w Mikołowie-Jamnie ( w Centralnych Warsztatach Autobusowych ) , MZA Warszawa i MPK Częstochowa , a na plac przeładunkowy Sosnowiec Południowy dostarczano gotowe karoserie z Węgier .
Historia modelu na rynku polskim .
Model `` Ikarus 280 '' był produkowany w wielu odmianach oznaczanych kombinacjami liczb i ewentualnie liter po kropce w ich nazwie .
W 1974 roku do WPK Katowice sprowadzone zostało 15 egzemplarzy wersji Ikarus 280.02 , która przeznaczona była na rynek NRD .
W 1978 roku rozpoczęto do Polski import modelu Ikarus 280.11 .
Do napędu tej wersji zastosowano 6-cylindrowy silnik wysokoprężny Raba-MAN D2156HM6U o mocy maksymalnej 142 kW ( 193 KM ) .
Jednostka napędowa zblokowana została z 5-biegową manualną skrzynią biegów Csepel ASH-75.2 .
