Ileodictyon cibarium
Ileodictyon cibarium Tul .
ex M. Raoul – gatunek saprotroficznych grzybów z rodziny sromotnikowatych ( `` Phallaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ileodictyon , Phallaceae , Phallales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy nazwy naukowej :
Występowanie .
Występuje głównie na Nowej Zelandii , gdzie określany jest potoczną nazwą `` basket fungus '' lub `` white basket fungus '' .
Jest gatunkiem rodzimym dla Nowej Zelandii i Australii .
Opisywano go także z Chile , oraz Wielkiej Brytanii i Wschodniej Afryki , gdzie został zawleczony
