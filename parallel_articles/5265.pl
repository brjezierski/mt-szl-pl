Ilich Ramírez Sánchez
Ilich Ramírez Sánchez , pseudonim bojowy `` Carlos '' , przez prasę nazywany `` Szakalem '' ( ur .
12 października 1949 w Michelenie ) – międzynarodowy terrorysta , który we Francji odbywa karę dożywotniego pozbawienia wolności .
Pseudonimy .
Ilich Ramírez Sánchez w mediach określany jest mianem „ Carlosa ” lub „ Szakala ” .
Pseudonimu bojowego „ Carlos ” zaczął używać już w 1970 roku , kiedy to przebywał pośród fedainów palestyńskich w Jordanii .
Pseudonim „ Szakal ” nadały mu media , po zabójstwie dwóch policjantów i współpracownika policji w 1975 roku .
Nazwa „ Szakal ” zaczerpnięta została z powieści Fredericka Forsytha „ Dzień Szakala ” o zawodowym zabójcy szykującym zamach na prezydenta Charles ’ a de Gaulle ’ a .
Życiorys .
Urodził się 12 października 1949 w miejscowości Michelena w Wenezueli .
Jego ojcem był José Altagracia Ramírez Naras – bogaty adwokat i zwolennik skrajnej lewicy .
Przyszły terrorysta imię Ilich otrzymał na cześć Włodzimierza Iljicza Lenina .
Również dwaj młodsi bracia przyszłego terrorysty odziedziczyli imiona po przywódcy rewolucji październikowej : w akcie urodzenia kolejnego syna widniało imię Lenin , a najmłodszego – Vladimir .
Ojciec wpajał synom komunistyczne poglądy już od najmłodszych lat , gdy Illich skończył 10 lat , został zapisany do organizacji młodzieżowej przy Komunistycznej Partii Wenezueli .
W dzieciństwie rodzina Ilicha często zmieniała miejsce zamieszkania .
Ramírezowie mieszkali m.in .
w Kingston , Meksyku czy Bogocie .
We wszystkich miastasch Ilich wraz z braćmi uczęszczał do najlepszych szkół .
W 1962 roku rodzina zamieszkała w Caracas , gdzie Ilich i Lenin ukończyli szkołę średnią .
W 1966 roku Ilich i Lenin dostali się na Uniwersytet w Caracas .
Rok później rodzice Ilicha rozwiedli się , matka wyprowadziła się do Londynu , zabierając tam też nastoletnich synów .
Mieszkając w Londynie , Ilich dobrze opanował język angielski i uczęszczał na zajęcia strzelectwa w klubie Chelsea .
W 1968 roku José Altagracia Ramírez Naras zdobył dla Ilicha i Lenina rekomendacje Komunistycznej Partii Wenezueli , by zostali oni przyjęci na studia na moskiewskim Uniwersytecie Patrice ’ a Lumumby .
Bracia Ramírez studiowali w Moskwie nauki techniczne .
Zaangażowali się przy tym w radykalny lewicowy ruch studencki .
W 1969 roku byli w gronie prowodyrów zamieszek pod ambasadą Iranu .
Wiosną 1970 roku Ilich i Lenin zostali relegowani z uniwersytetu .
Powodem ich wydalenia z uczelni były najprawdopodobniej względy ideologiczne .
Władze Związku Radzieckiego nie chciały pogorszyć relacji z Komunistyczną Partią Wenezueli – bracia Ramírez byli liderami wenezuelskich studentów , którzy opowiadali się za guevaryzmem , odrzuconym z kolei przez kierownictwo Komunistycznej Partii Wenezueli .
W lipcu 1970 roku Ilicz wyjechał do Jordanii , gdzie znajdowały się obozy marksistowskiego Ludowego Frontu Wyzwolenia Palestyny .
Niedoszły inżynier o działalności LFWP dowiedział się od Palestyńczyków uczących się w Moskwie , otrzymał od nich również list referencyjny .
Na miejscu przeszedł intensywne szkolenie wojskowe , do perfekcji opanowując szybkie strzelania z broni krótkiej , lewą ręką .
Już wtedy posługiwał się on pseudonimem „ Carlos ” .
Wziął też udział w konflikcie palestyńsko-jordańskim znanym jako Czarny Wrzesień .
W trakcie działań wojennych zdobył reputację nieustraszonego żołnierza i szacunek lidera LFWP Wadi Haddada .
Na początku 1971 roku został odesłany przez LFWP do Londynu .
„ Carlos ” zorganizował w mieście grupę arabskich radykałów i uruchomił sieć kryjówek dla bojowników Ludowego Frontu .
22 grudnia 1971 został aresztowany przez brytyjską policję .
Funkcjonariusze aresztowali go wraz z innymi ekstremistami palestyńskimi po zabójstwie ambasadora Jordanii w Wielkiej Brytanii .
Ramírez został zwolniony z aresztu po kilkunastu godzinach , kiedy okazało się , że posiada on wystarczające alibi .
Zamach na ambasadora przeprowadzili radykałowie z konkurencyjnej organizacji Czarny Wrzesień .
Po aresztowaniu „ Carlos ” pozostawał pod obserwacją policji , przez co do 1973 roku zawiesił działalność wywrotową .
W lipcu 1973 roku spotkał się z Wadi Haddadem w Bejrucie .
Haddad zgodził się powierzyć „ Carlosowi ” prowadzenie działalności terrorystycznej .
„ Carlos ” otrzymał polecenie zabicia żydowskiego biznesmena Josepha Sieffa , będącego prezesem sieci handlowej Marks & amp ; Spencer i działaczem syjonistycznym .
