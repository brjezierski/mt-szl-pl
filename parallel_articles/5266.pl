Język ili
Język ili — język wschodnioturecki używany w prefekturze Ili , ujgurskiego regionu autonomicznego Chińskiej Republiki Ludowej .
Liczba użytkowników w 1982 wynosiła 120 ludzi ; niemal wszyscy władali również kazachskim lub ujgurskim .
Po raz pierwszy został opisany w 1956 roku .
O ile należy do karłuckiej gałęzi języków turkijskich , to sąsiedztwo z Kazachami sprawia , że występują w nim wpływy kipczackie ( np .
asymilacja zrostku dopełniacza , przejście /ɣ/ w /w/ po samogłoskach otwartych ) .
Posiada 7 samogłosek i 22 spółgłoski .
Legenda głosi , że przodkowie dzisiejszych jego użytkowników przybyli do Chin ponad dwieście lat temu z kotliny fergańskiej .
Obecnie żyją w dorzeczu rzeki Ili oraz mieście Kuldża .
Z uwagi na liczne małżeństwa mieszane z Kazachami i Ujgurami , liczebność użytkowników języka ili zmniejsza się na rzecz kazachskiego i ujgurskiego .
