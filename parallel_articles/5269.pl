Illinois ( rzeka )
Illinois ( ang .
`` Illinois River '' ) – jeden z większych dopływów rzeki Missisipi , o długości 439 km i z dorzeczem 104 tys .
km² .
Przepływa skosem przez środkową część stanu Illinois .
W dawnych czasach ważna droga wodna Indian i francuskich traperów i handlarzy skórami , jako główny szlak wodny łączący Jezioro Michigan z rzeką Missisipi .
Pierwsze osady wzdłuż jej biegu tworzyły tzw .
`` Kraj Illinois '' .
Po wybudowaniu kanałów Illinois-Michigan i Hennepin w XIX wieku znaczenie żeglugowe rzeki zmalało .
Rzeka Illinois powstaje przez połączenie rzek Kankakee i Des Plaines na obszarze północnego stanu Illinois , ok. 16 km od miasta Joliet .
Stamtąd Illinois płynie na zachód , mijając miasteczka Morris i Ottawa , gdzie wpadają do niej dopływy Mazon i Fox ( rzeka ) .
W okolicach miasta La Salle wpada do niej dopływ Vermilion , a za Spring Valley , w hrabstwie Bureau , skręca gwałtownie na południe , mijając Peorię , największe miasto w jej biegu .
Na południe od Peorii dołącza do niej rzeka Mackinaw .
Potem przepływa przez rezerwat Chautauqua , zbiera jeszcze dopływy rzek Spoon , Sangamon , La Moine i Macoupin Creek , by wpaść do Missisipi w pobliżu miasteczka Grafton , ok. 40 km od St. Louis .
W rzeczywistości od Hennepin rzeka płynie starym korytem Missisipi , które przestało istnieć ok. 150 tys .
lat temu , gdy rozpoczęła się era lodowcowa .
Obecny bieg rzeki uformował się ok. 10 tysięcy lat temu .
W czasach ery nowożytnej bieg rzeki był domeną konfederacji plemion indiańskich znanej jako Illiniwekowie .
Francuzi pojawili się tu w roku 1673 .
Pierwszą europejską osadą była misja jezuicka założona przez o. Marquette w okolicach miejsca znanego jako `` Skała głodu '' ( ang .
`` Starved Rock '' , dzisiaj Park Stanowy ) , później przeniesiona do Creve Coeur w okolicach dzisiejszej Peorii .
W latach 1905-1915 rzeka dostarczała największych ( po rzece Kolumbia ) ilości ryb słodkowodnych w USA .
Dzisiaj , choć jej znaczenie zmalało , to jednak nadal jest jednym ze znaczących dostawców ryb oraz jako droga wodna dla transportu płodów rolnych na południe .
