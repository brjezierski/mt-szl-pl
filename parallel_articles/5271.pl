– gatunek grzybów należący do rzędu rozetkowcow ( `` Hypocreales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Illosporiopsis , Incertae sedis , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1986 r. B.L .
Brady i D. Hawksw .
nadając mu nazwę `` Hobsonia christiansenii '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 2001 r. D. Hawksw .
Synonimy :
Charakterystyka .
Grzyb naporostowy , pasożyt .
W polskim piśmiennictwie mykologicznym podano jego występowanie na następujących porostach : `` Bacidina '' , `` Melanohalea exasperatula '' , `` Parmelia sulcata '' , `` Phaeophyscia orbicularis '' , `` Physcia adscendens '' , `` Physcia stellaris '' , `` Physcia tenella '' , `` Xanthoria parietina '' oraz na innych , niezidentyfikowanych porostach .
Rozwija się zarówno na ich plesze , jak i na apotecjach .
Na zainfekowanych porostach wytwarza miękkie , różowe sporodochia z zarodnikami .
Na porostach z rodzaju `` Physcia '' i innych występuje pozornie podobny morfologicznie gatunek `` Marchandiomyces corallinus '' ( Roberge ) Diederich & amp ; D. Hawksw .
1990 .
Należy on jednak do podstawczaków .
Wytwarza znacznie bardziej solidne , jasnoróżowe koralkowate skleroty na martwych i umierających obszarach plechy gospodarza .
