Ilmajoki
Ilmajoki ( szw .
Ilmola [ ilmolɑ ] ) – gmina w Finlandii .
Jest jedną z 26 gmin regionu Ostrobotnia Południowa w dawnej prowincji Finlandia Zachodnia .
Liczba ludności ( 31 marca 2010 ) 11 717 , powierzchnia - 580 km² .
Gmina jest jednojęzyczna ( język fiński ) .
Posiada własne lotnisko lokalne .
Wioski należące do gminy : Ahonkylä , Huissi , Jouppila , Kiikerinkylä , Kirkonkylä , Koskenkorva , Könni , Luoma , Munakka , Nopankylä , Peltoniemi , Peräkylä , Peurala , Pojanluoma , Rengonkylä , Röyskölä , Seittunkylä , Tuomikylä , Ujaistenkylä , Varpahaiskylä .
