Historia .
Obszar , na którym położone jest Imabari , od dawna jest punktem strategicznym dla kontroli Morza Wewnętrznego .
W rezultacie był kontrolowany przez różne siły w okresie konfliktów .
Oficjalnie prawa miejskie uzyskało 11 lutego 1920 roku , po połączeniu z wioską Hiyoshi .
