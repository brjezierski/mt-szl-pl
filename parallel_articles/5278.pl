Immenstadt im Allgäu
Immenstadt im Allgäu , Immenstadt i. Allgäu – miasto w południowych Niemczech , w kraju związkowym Bawaria , w rejencji Szwabia , w regionie Allgäu , w powiecie Oberallgäu .
Leży w Allgäu , około 8 km na północny zachód od Sonthofen , nad rzeką Iller , przy drodze B19 , B308 i linii kolejowej Lindau ( Bodensee ) – Augsburg ; Oberstdorf – Ulm .
W mieście znajduje się stacja kolejowa Immenstadt .
Polityka .
Burmistrzem miasta jest Armin Schaupp ( bezpartyjny ) , rada miasta składa się z 24 osób .
