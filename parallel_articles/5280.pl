Imotski
Imotski ( , ) – miasto w Chorwacji , w żupanii splicko-dalmatyńskiej , siedziba miasta Imotski .
W 2011 roku liczyło 4757 mieszkańców .
Modro Jezero .
Na terenie miasta znajduje się Modro Jezero .
Jest to jezioro krasowe , którego głębokość dochodzi do 100 m i waha się w zależności od pory roku .
Wysokość leja krasowego jeziora wynosi 290 m. Modro Jezero ma długość ok. 800 m i szerokość ok. 500 m. Tafla jeziora , przy wysokim poziome wody , znajduje się na wysokości 342 m n.p.m .
Swoją nazwę jezioro zawdzięcza turkusowemu kolorowi wody .
Latem jezioro potrafi całkowicie wyschnąć , a na jego dnie rozgrywane są mecze piłkarskie pomiędzy drużynami Vilenjaci a Vukodlaci .
Do jeziora można zejść utwardzoną ścieżką .
Ścieżka powstała w 1907 roku po decyzji przebywającego w okolicy cesarza Austrii , Franciszka Józefa I. W jeziorze znajdują się trzy głazy , które wyznaczają poziom jeziora .
Gdy na głazie Katavić na wysokości 6 metrów pojawi się tafla wody , rozpoczyna się sezon pływacki .
Głaz Jakićin wyznacza środek sezonu pływackiego .
Natomiast głaz Pavić pojawia się pod koniec sezonu pływackiego .
