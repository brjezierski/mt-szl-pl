Inczon
Inczon ( kor .
인천 , zapisywane również jako Incheon , Inczhon ) – miasto na prawach metropolii i port morski w północno-zachodniej części Korei Południowej w pobliżu Seulu .
Wcześniej nosiło nazwę Jemulpo ( polska pisownia także Czemulpo ) .
Trzecie co do wielkości miasto kraju .
Historia .
Osadnictwo na ziemiach dzisiejszego Inczonu datuje się od czasów neolitu .
Położenie miasta pretendowało go do roli ważnego portu morskiego , założonego w 1883 roku ( populacja miasta liczyła w tym czasie 4700 mieszkańców ) .
W okresie tym miasto nosiło nazwę Jemulpo .
Podczas wojny rosyjsko-japońskiej , 9 lutego 1904 roku w Inczonie wylądowały wojska japońskie , opanowując następnie Koreę .
Równocześnie doszło do bitwy z okrętami rosyjskimi , znanej jako bitwa pod Czemulpo , zakończonej samozatopieniem dwóch rosyjskich okrętów .
Podczas wojny koreańskiej , 15 września 1950 roku Inczon był miejscem wielkiego desantu wojsk amerykańskich .
