Prowokacja gliwicka
Prowokacja gliwicka ( ) – niemiecka operacja dywersyjna typu `` false flag '' na niemiecką radiostację w Gliwicach , dokonana w czwartek 31 sierpnia 1939 o godz .
20:00 , wykorzystana przez niemiecką propagandę jako główny „ dowód polskich prowokacji ” .
Berlińskie radio informowało ponadto o kilku innych atakach rzekomych powstańców , których – już na terytorium Niemiec – wspierać miały regularne oddziały Wojska Polskiego , wyposażone w broń ciężką .
W ten sposób nazistowska propaganda już 31 sierpnia o godz .
22:30 obwiniła Rzeczpospolitą o rozpoczęcie wojny , a III Rzeszę przedstawiła jako ofiarę polskiej agresji .
Dla Hitlera incydenty graniczne stanowiły alibi ( „ od godziny 5:45 [ sic ] odpowiadamy ogniem ” ) , a dla Francji mogły stanowić pretekst do nieudzielenia Polsce militarnej pomocy .
Pewien udział w przygotowaniach do prowokacji gliwickiej miał m.in .
Niemiec sudecki Oskar Schindler , podówczas agent Abwehry .
Prowokacja gliwicka była jednym z serii przedsięwzięć Hitlera , mających zapobiec przekształceniu planowanej agresji III Rzeszy na Polskę w II wojnę światową , poprzez powstrzymanie sojuszników Rzeczypospolitej od wywiązania się z zobowiązań sojuszniczych wobec Polski , co by oznaczało dla Niemiec ryzykowną wojnę na dwa fronty .
Przebieg napadu .
Gliwice ( ) przed wojną należały do III Rzeszy , a granica z Polską przebiegała w odległości ok. 10 km od nowej radiostacji nadawczej ( ) , zbudowanej w roku 1935 .
Napastnicy nie wiedzieli , że w Gliwicach , w odległości ok. 4 km , była również druga radiostacja , zbudowana w roku 1925 .
W starej radiostacji ( przy obecnej ulicy Radiowej ) znajdowały się studia mikrofonowe .
W nowej ( przy ul .
Tarnogórskiej ) nie było żadnego studia .
Zainstalowano tam tylko urządzenia nadawcze do emisji sygnału , przesyłanego kablem teletechnicznym ze starej radiostacji lub z Wrocławia .
Na rozkaz Hitlera akcję – w ciągu trzech tygodni – przygotował szef ( tj urzędu nadrzędnego nad nazistowską służbą bezpieczeństwa ( , SD ) i policji bezpieczeństwa ( ) Reinhard Heydrich .
Na to zadanie wybrał dowódców SD , którym szczególnie ufał .
Wybór padł na oficera SD w stopniu SS-Sturmbannführera .
10 sierpnia Heydrich wezwał do siebie Naujocksa i poinformował o planach napaści na Polskę .
Wydał mu polecenia zainscenizowania na granicy polsko-niemieckiej kilku incydentów .
Pierwszym zadaniem było przebranie kilku niemieckich więźniów z długoletnimi wyrokami i ich zamordowanie .
Ciała następnie należało rozrzucić wzdłuż polsko-niemieckiej granicy , w taki sposób , by wyglądało to na atak polskich żołnierzy na posterunki niemieckie .
Naujocks udał się do hotelu w Gliwicach i czekał na rozkaz wkroczenia do akcji .
Ten jednak nie następował .
12 sierpnia Hitler wyznaczył termin ataku na Polskę na 26 sierpnia , jednak 25 sierpnia termin ten przesunięto na 1 września na 4:30 .
31 sierpnia po południu Heydrich uruchomił akcję hasłem – ( pol .
„ Babcia umarła ” ) .
Następnie – na osobisty telefoniczny rozkaz Himmlera – usunięto ochronę radiostacji .
O godz .
18.00 skierowano tam dwóch funkcjonariuszy policji , z których jeden miał wpuścić napastników przez furtkę w ogrodzeniu , a drugi wszedł do środka , sfraternizował się z załogą i w czasie napadu – jako zaaresztowany towarzysz niedoli pracowników – pilnował , by ci nie postąpili „ nierozważnie ” .
Pomiędzy godziną 19 i 20 oddział Naujocksa złożony z siedmiu osób opuścił hotel bez dokumentów ( na wypadek ewentualnego aresztowania ) i w zniszczonych ubraniach .
Udawali oni cywilnych powstańców śląskich .
O godzinie 20 weszli przez tylne drzwi do budynku radiostacji gliwickiej i bez przeszkód dotarli do sali nadajnika , gdzie pracowało trzech techników w asyście policjanta .
Wszyscy słuchali wiadomości wieczornych , nadawanych i retransmitowanych z Wrocławia o 20.00 .
Pracownicy zostali skrępowani i zamknięci w piwnicy .
Wówczas oddział napotkał nieprzewidziane problemy .
Nie udało im się uruchomić urządzeń nadawczych .
W skład oddziału wchodził wykwalifikowany radiotechnik , ale jemu również nie udało się wyemitować sygnału .
Był on zagłuszany przez silniejszy nadajnik położony 150 kilometrów dalej , we Wrocławiu .
Po trwających ponad 10 minut poszukiwaniach po groźbach pobicia zmuszono załogę do wydania tzw .
