Indian Wells ( Kalifornia )
Indian Wells – miasto w stanie Kalifornia , w Stanach Zjednoczonych .
Populacja : 3816 ( 2000 ) .
Powierzchnia : 37,79 km² .
Miasto jest położone mniej więcej w połowie drogi wytyczonej fragmentem autostrady I-10 , przebiegającym nieco na północ od centrów miast Palm Springs ( słynnego kurortu golfowego ) i Indio ( siedziby kwietniowego festiwalu muzyki alternatywnej Coachella Valley Music and Arts Festival ) , w Dolinie Coachella , naturalnie pustynnym , sztucznie nawadnianym rejonie o intensywnym rolnictwie , zamieszkanym przez ok. milion mieszkańców .
Uprawiano tu kiedyś daktyle , przed boomem deweloperskim i napływem zamożnych mieszkańców .
