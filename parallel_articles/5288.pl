Indianapolis
Indianapolis – miasto w Stanach Zjednoczonych , stolica i największe miasto stanu Indiana , leżące w hrabstwie Marion , nad rzeką White .
Demografia .
Miasto ma zróżnicowaną strukturę demograficzną .
Większość ( 69,9 % ) stanowią biali .
Za nimi plasują się Afroamerykanie , którzy stanowią ponad 25 % ludności miasta .
Prawie 4 % ludności stanowią Latynosi i 1,5 % Indianie .
25,7 % ludności stanowią dzieci do 18. roku życia , a 11 % ludzie powyżej 65 lat .
Średnie zarobki w tym mieście wynoszą około 40 tysięcy dolarów rocznie .
