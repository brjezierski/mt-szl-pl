Indore
Indore ( hindi इंदौर ) – miasto w środkowych Indiach , w stanie Madhya Pradesh , w górach Windhja .
Około 1,8 mln mieszkańców .
Założone zostało w 1715 roku .
Ważny ośrodek przemysłu włókienniczego ( głównie bawełnianego ) i chemicznego , nadto przemysł metalowy , materiałów budowlanych , papierniczy , spożywczy .
Ośrodek handlowy regionu uprawy bawełny .
Węzeł kolejowy i drogowy , port lotniczy .
Uniwersytet , szkoła handlowa .
Zabytki architektury z XVIII-XIX w. , w tym m.in .
pałac Juna Rawadżada i Lal-Bagh ( wzorowany na pałacu w Wersalu ) .
