`` Republic of India '' ) – państwo położone w Azji Południowej , zajmujące większość subkontynentu indyjskiego .
Jest drugim najludniejszym państwem świata pod względem liczby ludności oraz siódmym pod względem powierzchni ; jest także najludniejszą demokracją świata .
Od południa Indie otoczone są Oceanem Indyjskim , od południowego zachodu Morzem Arabskim , a od południowego wschodu Zatoką Bengalską .
Od północy Indie ograniczone są przez Himalaje oraz Karakorum .
Na północnym zachodzie graniczą z Pakistanem , na północy z Chinami , Nepalem i Bhutanem , na północnym wschodzie z Mjanmą i Bangladeszem , a na południowym wschodzie przez zatokę Mannar i cieśninę Palk graniczy ze Sri Lanką i Malediwami .
Indyjskie wyspy Andamany i Nikobary dzielą granicę morską z Tajlandią i Indonezją .
W 2017 r. gospodarka indyjska była 6. na świecie pod względem PKB nominalnego oraz 3. pod względem parytetu siły nabywczej .
Po reformach liberalizujących gospodarkę kraju z 1991 Indie stały się jednym z najszybciej rozwijających się krajów świata oraz zostały zaliczone do krajów nowo uprzemysłowionych .
Jednak nadal mierzy się z problemem ubóstwa , korupcji , niedożywienia oraz słabo rozwiniętej publicznej opieki zdrowotnej .
Indie posiadają broń jądrową oraz uważane są za mocarstwo regionalne .
Indyjska armia jest 2. na świecie pod względem liczebności wojska oraz 4. pod względem wydatków na obronność .
Indie są parlamentarną republiką federalną podzieloną na 28 stanów i 7 terytoriów .
Kraj ten słynie z kinematografii , bogatej kuchni oraz bujnego ekosystemu .
Geografia .
Od północy Indie są ograniczone przez łańcuchy górskie : Karakorum i Himalaje .
Z Indiami graniczą : Pakistan na północnym zachodzie , Chiny , Nepal oraz Bhutan na północy , Mjanma i Bangladesz na północnym wschodzie , Sri Lanka przez zatokę Mannar i cieśninę Palk na południowym wschodzie .
Od południowego zachodu kraj otaczają wody Morza Arabskiego , zaś od południowego wschodu Zatoki Bengalskiej i Morza Andamańskiego .
Do Indii należy archipelag Lakkadiwów leżący na Morzu Arabskim 450 km na zachód od wybrzeży kraju oraz leżące w Zatoce Bengalskiej archipelagi Andamanów i Nikobarów .
Najwyższym punktem jest ośmiotysięcznik Kanczendzonga położony w Himalajach na granicy z Nepalem wznoszący się na wysokość 8586 m n.p.m .
Rzeki .
Rzeki Indii należą do zlewiska Oceanu Indyjskiego .
