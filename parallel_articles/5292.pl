Prądy powierzchniowe .
W części północnej oceanu prądy morskie są zależne od monsunów i mają charakter sezonowy ( Prąd Somalijski , Monsunowy i Równikowy Wsteczny ) .
Ciepłe prądy stałe to : Prąd Południoworównikowy , Prąd Madagaskarski , Prąd Mozambicki , natomiast zimne : Prąd Zachodnioaustralijski oraz Prąd Wiatrów Zachodnich .
Gospodarka .
Ocean Indyjski jest ważną drogą łączącą Bliski Wschód , Afrykę , południową Azję i Australię .
Jest szlakiem przewozu ropy naftowej oraz produktów ropopochodnych pochodzących z rejonu Zatoki Perskiej i Indonezji .
Rybołówstwo jest dość słabo rozwinięte i zaspokaja głównie potrzeby lokalne .
Poławia się przeważnie tuńczyki i krewetki .
