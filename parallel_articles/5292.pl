Ocean Indyjski
Ocean Indyjski ( Indyk ) – trzeci pod względem wielkości zbiornik wodny na świecie , zajmujący około 20 % powierzchni Wszechoceanu .
Oficjalna polska nazwa tego oceanu , zatwierdzona przez Komisję Standaryzacji Nazw Geograficznych brzmi Ocean Indyjski .
Położenie i obszar .
Ocean od północy ograniczony jest przez Azję , od zachodu przez Półwysep Arabski i Afrykę , od wschodu przez Półwysep Malajski , Wyspy Sundajskie oraz Australię , a od południa przez Antarktydę ( Ocean Południowy ) .
Umownymi granicami są południki : 20° i 147° długości geograficznej wschodniej .
Ocean ten ma powierzchnię 73 556 000 km² ( włączając Morze Czerwone i Zatokę Perską ) ; przybliżona objętość wynosi 292 130 000 km³ .
Rozciągłość południkowa i równoleżnikowa dochodzi do 10 000 km .
Średnia głębokość 3890 m , maksymalna 7258 m ( Rów Sundajski ) .
Linia brzegowa o długości 66 526 km jest słabo rozwinięta .
Ocean Indyjski jest bardzo ciepły , w północnej części temperatura wód powierzchniowych rzadko spada poniżej 25 °C , natomiast w pobliżu Antarktydy obniża się do ok. 0 °C , co daje średnią na poziomie 17 °C .
Zasolenie wynosi 34,8 promila .
