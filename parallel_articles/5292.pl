Baseny oceaniczne .
Na Oceanie Indyjskim znajdują się następujące baseny oceaniczne : Basen Arabski , Somalijski , Madagaskarski , Maskareński , Mozambicki , Crozeta , Środkowoindyjski , Kokosowy , Zachodnioaustralijski , Północnoaustralijski i Południowoaustralijski .
W obrębie basenów widoczne są oceaniczne strefy spękań , leżące na przedłużeniu uskoków transformacyjnych .
Wiek skorupy ziemskiej .
Wiek najstarszej skorupy oceanicznej na Oceanie Indyjskim określono jako jurajski .
Występuje ona w dwóch rejonach : na krańcu wschodnim , między Australią a Indonezją oraz w części zachodniej – pomiędzy Afryką a Madagaskarem ( pod Basenem Mozambickim , Madagaskarskim i Kanałem Mozambickim ) .
Wiek pozostałej skorupy ziemskiej jest kredowy i kenozoiczny , przy czym im bliżej szczeliny ryftowej , tym jest młodszy .
Większość grzbietów śródoceanicznych pochodzi z ery kenozoicznej .
Klimat .
Warunki klimatyczne Oceanu Indyjskiego kształtują się pod wpływem wzajemnego oddziaływania kontynentu i oceanu .
Silne , północno-wschodnie wiatry wieją od października do kwietnia ; od maja do października dominują wiatry południowe i wschodnie .
Na półkuli południowej wiatry są raczej łagodniejsze , ale letnie sztormy w pobliżu Mauritiusa mogą być niebezpieczne .
Cyklony występują u wybrzeży Morza Arabskiego i Zatoki Bengalskiej w miesiącach wymiany monsunów zimowego i letniego .
