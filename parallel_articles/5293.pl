Język indonezyjski ( indonez . )
– język z rodziny austronezyjskiej , od 1949 roku objęty statusem języka urzędowego w Indonezji .
Posługuje się nim 197,7 mln mieszkańców tego kraju , z czego dla 42,8 mln osób jest to język ojczysty .
W Indonezji język indonezyjski spełnia wszelkie funkcje języka państwowego – jest językiem komunikacji urzędowej , powstaje w nim literatura oraz większość tekstów oficjalnych .
Jest używany jako lingua franca wśród różnych grup etnolingwistycznych .
