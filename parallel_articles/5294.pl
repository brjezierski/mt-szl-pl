Indonezja
Indonezja , oficjalnie Republika Indonezji ( ) – wyspiarskie państwo unitarne położone w Azji Południowo-Wschodniej oraz w Oceanii , którego stolicą jest Dżakarta .
Położona na równiku , między Oceanem Spokojnym a Indyjskim , jest największym krajem wyspiarskim na świecie z ok. 17 tys .
wysp .
Jest 14. pod względem powierzchni krajem i 7. wliczając tereny morskie .
Jest 4. krajem na świecie pod względem liczby ludności , z ponad 275-milionową populacją oraz największą liczbą wyznawców islamu na świecie .
Najbardziej zaludnioną wyspę na świecie , Jawę , zamieszkuje ponad połowa ludności kraju .
Indonezja składa się z setek różnych rodzimych grup etnicznych i językowych , z których największą jest jawajska .
Wspólna tożsamość rozwinęła się zgodnie z mottem `` Bhinneka Tunggal Ika '' ( „ Jedność w różnorodności ” , dosłownie „ wielu , ale jeden ” ) , zdefiniowanym przez język narodowy , różnorodność etniczną , pluralizm religijny w populacji muzułmańskiej oraz historię kolonializmu i buntu przeciwko niemu .
Gospodarka Indonezji zajmuje 16. miejsce na świecie pod względem nominalnego PKB i 7. pod względem PKB według siły nabywczej .
Indonezja jest mocarstwem regionalnym w Azji Południowo-Wschodniej .
Pomimo dużej populacji i gęsto zaludnionych regionów Indonezja ma rozległe obszary dzikiej przyrody , co zapewnia krajowi dużą różnorodność biologiczną , nawet jeśli dziedzictwo to zmniejsza się z powodu gwałtownie rosnącej działalności człowieka .
