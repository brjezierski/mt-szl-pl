Etymologia nazwy kraju .
Nazwa `` Indonezja '' wywodzi się z dwóch greckich słów : `` Indos '' – „ indyjski ” i , `` nesos '' – „ wyspa ” .
Nazwa ta pochodzi z XIX wieku , na długo przed powstaniem niepodległej Indonezji .
Była najpierw używana do określenia terytorium , a następnie także bytu politycznego i państwa .
W 1847 r. angielski etnolog George Earl ukuł termin `` Indu-nesians '' na określenie mieszkańców archipelagu indonezyjskiego i filipińskiego , a także półwyspu Malakka .
Jeden z jego uczniów , James Richardson Logan , w 1850 r. użył nazwy „ Indonezja ” jako synonimu „ indyjskiego archipelagu ” .
Od początku XX w. nazwa „ Indonezja ” była powszechnie używana przez zagranicznych i holenderskich naukowców , a także przez indonezyjskie grupy nacjonalistyczne .
Historia .
Prehistoria .
Skamieniałe szczątki Homo erectus , znanego jako człowiek jawajski , sugerują , że archipelag indonezyjski był zaludniony 2 miliony lat temu .
Na wyspie Flores znaleziono wymarły gatunek hominidów : człowieka z Flores ( Homo floresiensis ) .
W czasie zlodowacenia poziom morza był niższy niż obecnie , a zachodnia część archipelagu indonezyjskiego , będąca częścią szelfu kontynentalnego zwanego Sundą , była wówczas połączona z lądem .
