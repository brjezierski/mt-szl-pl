Poza tym użytkownicy inguskiego zamieszkują w niewielkich skupiskach także na terytorium Uzbekistanu , Kazachstanu , Turkmenistanu Turcji i Jordanii , a także w państwach Europy Zachodniej .
Język inguski genetycznie najbliżej spokrewniony jest z językiem czeczeńskim oraz wymierającym językiem bacbijskim .
Wśród języków kaukaskich inguski wyróżnia się bardzo rozbudowanym systemem konsonantów ( spółgłoski proste , geminowane , ejektywne , faryngalne ) .
Od początku lat 20 .
XX w. podjęto próbę stworzenia inguskiego języka literackiego .
Wcześniej teksty inguskie zapisywane były sporadycznie , niemal wyłącznie pismem arabskim .
Na potrzeby języka literackiego stworzony został alfabet , oparty na łacince .
Był on w użyciu do 1938 r. , kiedy to wprowadzona została , jako nowy obowiązujący alfabet , zmodyfikowana cyrylica .
