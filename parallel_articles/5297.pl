Język inguski
Język inguski ( ing .
гӀалгӀай мотт , Ğalğaj mott ) – język Inguszów , zamieszkujących głównie autonomiczną republikę Inguszetię , wchodzącą w skład Federacji Rosyjskiej .
Należy do języków nachskich ( nachijskich ) , tworzących podgrupę wśród języków nachsko-dagestańskich ( północno-wschodnich ) w wielkiej rodzinie języków kaukaskich .
Na terenie Inguszetii język inguski jest wraz z językiem rosyjskim oficjalnym językiem urzędowym .
Językiem inguskim posługuje się według różnych danych , od ok. 200 tys .
