Innsbruck
Innsbruck – miasto statutarne w Austrii , stolica kraju związkowego Tyrol oraz powiatu Innsbruck-Land , do którego jednak miasto nie należy .
Położone jest na wysokości 574 m n.p.m .
w dolinie rzeki Inn .
Liczy 126 851 mieszkańców ( 1 stycznia 2015 ) .
Około 30 km na południe od miasta znajduje się przełęcz Brenner ( 1370 m n.p.m .
) .
Miasto było dwukrotnie organizatorem zimowych igrzysk olimpijskich ( w latach 1964 i 1976 ) .
Innsbruck organizował też Mistrzostwa Świata w Narciarstwie Klasycznym 1933 , Mistrzostwa Świata w Narciarstwie Alpejskim 1933 i Mistrzostwa Świata w Narciarstwie Alpejskim 1936 oraz był jednym z miast organizujących Euro 2008 .
Historia .
Pierwsze zapiski o mieście pochodzą z 1187 roku i opisują je jako miejsce postoju w pobliżu przełęczy Brenner , najłatwiejszej wówczas drogi łączącej północ Europy z Włochami .
Innsbruck szybko się rozwijał i już w 1239 r. otrzymał prawa miejskie od księcia Meranii .
