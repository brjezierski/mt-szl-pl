Rysostrzępiak piaskowolubny
Rysostrzępiak piaskowolubny ( `` Pseudosperma arenicola '' ( R. Heim ) Matheny & amp ; Esteve-Rav . )
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudosperma , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1931 r. Roger Heim jako formę rysostrzępiaka porysowanego `` Inocybe fastigiata '' f. `` arenicola '' .
Marcel Bon w 1983 r. podniósł go do rangi gatunku jako `` Inocybe arenicola '' .
W 2019 r. P.D .
Matheny i F. Esteve-Rav .
