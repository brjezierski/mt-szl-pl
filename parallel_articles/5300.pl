Strzępiak owocowy
Strzępiak owocowy ( `` Inocybe bresadolae '' Massee ) – gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Inocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim : `` Astrosporina bresadolae '' ( Massee ) E. Horak 1979 :
Nazwę polską zaproponował Władysław Wojewoda w 1990 r. W monografii strzępiaków Andrzeja Nespiaka opisany jest jako strzępiak Bresadoli .
Morfologia .
Średnica do 3-4 , wyjątkowo do 6 cm , początkowo stożkowaty , potem półkulisty , na koniec rozpostarty , z małym garbkiem .
Brzeg początkowo podgięty , potem odchylający się ku górze .
