Występowanie i siedlisko .
`` Inocybe catalaunica '' znany jest tylko w niektórych krajach Europy .
Andrzej Nespiak w monografii strzępiaków Polski nie wspomina o nim .
Na terenie Polski jego stanowisko podano później ( w 2005 r. ) w okolicach Olkusza .
Bardziej aktualne stanowiska tego gatunku podaje internetowy atlas grzybów .
`` I catalaunica '' zaliczony w nim jest do grupy grzybów chronionych i zagrożonych .
Gatunki podobne .
Nasza wiedza o tym gatunku jest ograniczona do holotypu .
Można go łatwo odróżnić od innych podobnych gatunków przez migdałowate zarodniki z jednym wyrostkiem i niesymetryczną budowę ; jedna strona jest bardziej wypukła od drugiej .
Ma też dość cienkościenne kaulocystydy .
