Włośniak włosistopierścieniowy
Włośniak włosistopierścieniowy ( `` Mallocybe heimii '' ( Bon ) Matheny & amp ; Esteve-Rav . )
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mallocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1984 r. Marcel Bon nadając mu nazwę `` Inocybe heimii '' .
W 2019 r. P.D .
Matheny i F. Esteve-Rav .
przenieśli go do rodzaju `` Mallocybe '' .
