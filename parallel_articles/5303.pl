Morfologia .
Średnica 1,5–3 cm , początkowo stożkowo-dzwonowaty do wypukłego , potem płaski z garbkiem , brzeg często z resztkami zasnówki , higrofaniczny .
Powierzchnia ciemnobrązowa , zwykle popękana , bardzo łuskowata .
Karbowane , wolne , brzuchate , początkowojasnobrązowe , potem ciemniejące od zarodników .
Ostrza białawe .
Cylindryczny , stosunkowo krótki , zakrzywiona u podstawy , koloru brązowego , pokryty białawym nalotem .
Cienki , jasny ( biało-kremowo-beżowy ) .
Zarodniki bardzo wydłużone , podobne do zarodników borowików .
Występowanie i siedlisko .
Znane jest występowanie `` Inocybe impexa '' w niektórych krajach Europy .
Gatunku tego brak w monografii strzępiaków Andrzeja Nespiaka oraz w wykazie grzybów wielkoowocnikowych W. Wojewody .
Jego nowe stanowiska w Polsce podali m.in .
D. Karasiński i M. Wołkowycki w 2015 r .
