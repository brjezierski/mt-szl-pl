Strzępiak dziwaczny
Strzępiak dziwaczny ( `` Inocybe oblectabilis '' ( Britzelm . )
Sacc . )
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Inocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1890 r. Max Britzelmayr nadając mu nazwę `` Agaricus oblectabilis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Pier Andrea Saccardo w 1895 r .
