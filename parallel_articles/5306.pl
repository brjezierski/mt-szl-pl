Rysostrzępiak perłowy
Rysostrzępiak perłowy , strzępiak perłowy ( `` Pseudosperma perlatum '' ( Cooke ) Matheny & amp ; Esteve-Rav . )
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudosperma , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1886 r. Mordecai Cubitt Cooke nadając mu nazwę `` Agaricus perlatus '' .
Pier Andrea Saccardo w 1887 r. przeniósł go do rodzaju `` Inocybe '' , nadając mu nazwę `` Inocybe perlatum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Matheny i Esteve-Rav .
