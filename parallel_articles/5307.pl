Strzępiak ciemnowierzchołkowy
Strzępiak ciemnowierzchołkowy ( `` Inocybe phaeodisca '' Kühn ) – gatunek niejadalnych grzybów z rodziny strzępiakowatych ( `` Inocybaceae '' ) .
