Strzępiak żółtoblaszkowy
Strzępiak żółtoblaszkowy ( `` Inocybe relicina '' ( Fr . )
Quél . )
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Inocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1821 r. Elias Fries nadając mu nazwę `` Agaricus relicinus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Lucien Quélet w 1888 r .
