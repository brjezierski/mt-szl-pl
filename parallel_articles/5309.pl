Inocybe rennyi
Inocybe rennyi ( Berk .
& amp ; Broome ) Sacc .
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Inocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1879 r. Miles Joseph Berkeley i Christopher Edmund Broome nadając mu nazwę `` Agaricus rennyi '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Pier Andrea Saccardo w 1887 r .
Synonimy :
Morfologia .
Średnica 0,6–2,5 cm , początkowo szeroko wypukły , potem w miarę dojrzewania coraz bardziej rozpostarty , w końcu niemal płaski , zazwyczaj z garbkiem .
Brzeg prosty , zazwyczaj ostry .
