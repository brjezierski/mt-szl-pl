Strzępiak promienisty
Strzępiak promienisty ( `` Inocybe splendens '' R. Heim ) – gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Inocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską nadał Andrzej Nespiak w 1990 roku .
W opracowaniu Władysława Wojewody z 2003 r. gatunek ten jest synonimizowany z `` Inocybe phaeoluca '' Kühner , jednak według Index Fungorum są to odrębne gatunki .
Morfologia .
Średnica 3–5 cm , początkowo stożkowaty , potem rozpostarty z wyraźnym , tępym garbkiem .
Brzeg podgięty .
Powierzchnia promieniście popękana i pokryta ciemnobrązowymi , liliowymi lub purpurowo-czarniawymi , przylegającymi włókienkami .
Spomiędzy włókienek prześwituje słomkowe tło .
Przyrośnięte , lub wyraźnie zbiegające średnio gęste , szerokie .
Początkowo o barwie od białawej do płoworóżowej , potem ciemniejsze od zarodników .
Wysokość 3–5,5 cm , grubość 0,7–1,1 cm , walcowaty , z obrzeżoną bulwką w podstawie .
Powierzchnia biała , na szczycie oszroniona .
