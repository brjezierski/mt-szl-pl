Strzępiak białawy
Strzępiak białawy , strzępiak cienisty ( `` Inocybe umbratica '' Quél . )
– gatunek grzybów należący do rodziny strzępiakowatych ( `` Inocybaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Inocybe , Inocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 roku .
Andrzej Nespiak w 1990 roku opisał go pod nazwą strzępiak cienisty .
Morfologia .
Średnica do 2,5 cm , początkowo stożkowaty , potem rozpostarty z wyraźnym , tępym garbkiem .
Brzeg początkowo podgięty , potem wyprostowujący się i promieniście pękający .
Powierzchnia początkowo biaława , potem szarobeżowa z żółtawym odcieniem , jedwabista , pokryta delikatnymi włókienkami , lepiej widocznymi przy brzegu .
