Inta
Inta ( ros .
Инта ) – miasto w północnej Rosji , na terenie wchodzącej w skład tego państwa Republiki Komi , w północno-wschodnim skrawku kontynentu europejskiego .
Miejscowość liczy 32 080 mieszkańców ( 2010 r. ) .
W przeszłości znajdował się tam obóz Gułagu .
Osadzeni byli w nim m.in .
Janka Hińko , Józef Święcicki , kard .
Kazimierz Świątek oraz Andrzej Niezabitowski , który pozostawił opis swojego pobytu w Incie w książce `` To był tylko etap '' ( Kraków 1999 ) .
W mieście rozwinął się przemysł maszynowy oraz drzewny .
