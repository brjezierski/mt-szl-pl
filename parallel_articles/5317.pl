Międzynarodowy znormalizowany numer książki
Międzynarodowy znormalizowany numer książki ( ang .
`` International Standard Book Number '' , ISBN ) – niepowtarzalny 13-cyfrowy identyfikator książki , do 31 grudnia 2006 roku zawierał 10 cyfr .
Według zaleceń standardu numer ten powinien identyfikować wydawcę , jak również konkretny tytuł oraz edycję ( wydanie ) .
Historia standardu .
System ISBN został utworzony w Wielkiej Brytanii w 1966 roku ( został wtedy nazwany „ standardowym numerowaniem książek ” – `` Standard Book Numbering '' , SBN ) i przyjęty jako międzynarodowy standard ISO 2108 w 1970 roku .
Obecnie 159 krajów i terytoriów jest oficjalnymi członkami ISBN .
Obowiązujący obecnie standard jest rozszerzeniem standardu dotychczasowego – do numerów dziesięciocyfrowych dodano prefiks `` 978 '' , a znak kontrolny został przeliczony ponownie według algorytmu numeracji Bookland/ISBN wykorzystywanej w kodach kreskowych EAN-13 .
Po wykorzystaniu wszystkich numerów prefiks 978 zmieniony zostanie na 979 .
W ten sposób zgodnie z zaleceniami ISO zwiększono zakres liczbowy numeracji i dokonano ujednolicenia standardów .
ISBN w Polsce .
ISBN nadawany jest w Polsce od 1979 roku .
Wyszukiwarki mogą wprowadzać w błąd podając rok pierwszego wydania , jeszcze bez numeru ISBN .
Używanie ISBN w opisie bibliograficznym w Polsce usankcjonowano Polską Normą PN-82/N-01152.01 w 1982 roku .
Krajowe Biuro ISBN poinformowało , że 1 stycznia 2007 roku wchodzi w życie zaktualizowana międzynarodowa norma ISO 2108 , zgodnie z którą numery ISBN zmienia się z 10-cyfrowych na 13-cyfrowe .
