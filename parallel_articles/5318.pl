Internet
Internet ( skrótowiec od , dosłownie „ między-sieć ” ) – ogólnoświatowy system połączeń między komputerami , określany również jako sieć sieci .
W definicji informatycznej Internet to przestrzeń adresów IP przydzielonych hostom i serwerom połączonym za pomocą urządzeń sieciowych , takich jak karty sieciowe , modemy i koncentratory , komunikujących się za pomocą protokołu internetowego z wykorzystaniem infrastruktury telekomunikacyjnej .
Geneza i historia Internetu .
1963–1983 .
Początki Internetu wiążą się z powstaniem sieci rozległej ARPANET i sięgają końca lat 60 .
XX wieku .
Powszechnie uważa się , iż potrzeba jego stworzenia była konsekwencją prac amerykańskiej organizacji badawczej RAND Corporation .
Na podstawie uzyskanych przez nią raportów miano podjąć prace projektowe nad skonstruowaniem sieci komputerowej mogącej funkcjonować mimo częściowego zniszczenia .
Charles Herzfeld , dyrektor ARPA w czasach powstania ARPANET , obala jednak tak rozumiany mit genezy Internetu i zauważa , że od początku chodziło wyłącznie o zwiększenie potencjału naukowego przez połączenie oddalonych od siebie placówek badawczych wyposażonych w komputery .
1984–1995 .
W latach 80 .
Anglik Tim Berners-Lee stworzył podwaliny dzisiejszej usługi WWW .
Była to wówczas przestrzeń , gdzie przechowywane informacje należało odszukiwać , wpisując adres URL zaczynający się od http .
W 1985 roku została zarejestrowana pierwsza domena z rozszerzeniem .com pod nazwą Symbolics .
Trzy lata później ( 1988 ) Robert Morris po raz pierwszy w historii zainfekował Internet wirusem zwanym powszechnie Morris Worm .
Od 1993 do 1995 roku pojawiły się takie wydarzenia jak pierwszy streaming , pierwsza przeprowadzona transakcja finansowa w trybie online oraz tworzenie pierwszych blogów wraz z banerami graficznymi w przestrzeni internetowej .
Terminologia .
Internet a WWW ( World Wide Web ) .
Internet w ogólnym znaczeniu to sieć komputerowa , czyli wiele połączonych ze sobą komputerów , zwanych również hostami , natomiast WWW to usługa internetowa .
Innymi znanymi usługami tego rodzaju są : poczta elektroniczna oraz P2P .
Rozróżnienie pomiędzy siecią a usługami odwołuje się do pojęcia protokołów internetowych , będących określonym zbiorem protokołów komunikacyjnych .
Każdy protokół komunikacyjny jest logicznie podzielony na tzw .
warstwy : warstwa niższa zapewnia funkcjonalność wymaganą przez najbliższą warstwę wyższą , dodając jednocześnie własne dane pomocnicze i kontrolne .
Internet to sieć komputerowa wykorzystująca protokół IP ( ang .
`` Internet Protocol '' ) .
Natomiast usługa WWW wykorzystuje protokół HTTP ( ang .
`` HyperText Transfer Protocol '' ) , działający w warstwie wyższej , dla której nośną jest warstwa protokołu IP .
Protokół HTTP określany jest też mianem „ protokołu warstwy aplikacji ” z tego względu , że stanowi on warstwę nośną dla danych tworzonych przez oprogramowanie użytkowe , zwane aplikacjami .
