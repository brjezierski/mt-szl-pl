Serial internetowy
Serial internetowy – rodzaj serialu , który jest dystrybuowanego za pomocą internetu .
Produkcje takie są dostępne np .
na YouTubie , w przeciwieństwie do seriali telewizji internetowych .
Czas trwania odcinka jest różny dla każdej produkcji , podobnie jest z liczbą odcinków .
W odróżnieniu od serialu telewizyjnego , czas emisji odcinka serialu internetowego nie zawsze jest stały .
Seriale takie tworzone są zarówno przez amatorów , jak i przez profesjonalistów .
Przykładem takiej produkcji jest amerykański miniserial internetowy `` '' , wyprodukowany w oparciu o komiksy wydawnictwa Marvel Comics .
Seriale anime rozpowszechniane w ten sposób nazywane są ONA .
