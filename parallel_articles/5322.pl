Ion Iliescu
Ion Iliescu ( wym .
; ur .
3 marca 1930 w Oltenița ) – rumuński polityk , wieloletni działacz komunistyczny , minister do spraw młodzieży ( 1967–1971 ) , jedna z głównych postaci rewolucji z 1989 , lider Frontu Ocalenia Narodowego , tymczasowa głowa państwa w latach 1989–1990 , prezydent Rumunii w latach 1990–1996 i 2000–2004 , przewodniczący ugrupowań postkomunistycznych i senator .
Życiorys .
Działalność do 1989 .
Urodził się jako syn kolejarza i działacza Rumuńskiej Partii Komunistycznej .
W młodości wstąpił do związanej z komunistami organizacji młodzieżowej Uniunea Tineretului Comunist .
Z wykształcenia inżynier , ukończył Instytut Politechniczny w Bukareszcie , po czym kształcił się na Uniwersytecie Moskiewskim .
Od 1955 pracował jako projektant w Institutul de Studii si Proiectari Energetice , instytucie badawczym sektora energetycznego .
W 1948 i 1956 współtworzył dwie organizacje studenckie .
Od 1949 do 1956 był członkiem komitetu centralnego komunistycznej młodzieżówki Uniunea Tineretului Muncitor , w tym od 1954 jego sekretarzem .
