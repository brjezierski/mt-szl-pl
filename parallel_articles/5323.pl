Iowa City
Iowa City – miasto w Stanach Zjednoczonych , we wschodniej części stanu Iowa , nad rzeką Iowa ( dopływem Missisipi ) .
Pierwsza stolica stanu ( do 1857 ) .
Ośrodek handlowy regionu rolniczego , przemysłu elektronicznego , komputerowego , poligraficznego , papierniczego oraz naukowego .
W mieście znajduje się uniwersytet stanowy ( University of Iowa ) , założony w 1847 r .
