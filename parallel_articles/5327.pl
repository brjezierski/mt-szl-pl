Etymologia .
Nazwa w języku arabskim العراق ( `` al-ʿIrāq '' ) poświadczona została już w VI wieku .
Spośród kilkunastu etymologii , jedna odnosi się do starożytnego miasta Uruk w południowej Mezopotamii ( sum .
unug/unuki ; akad .
`` Uruk '' ; bibl .
`` Erech '' ) , inna zaś do arabskiego słowa `` ’ araqa '' , „ spocony , głęboko zakorzeniony , dobrze podlany ” ( dobrze nawodniony obszar mógł robić wrażenie na Arabach pochodzących z pustynnych terenów Półwyspu Arabskiego ) .
Arabski podróżnik Al-Masudi ( zm .
956 ) twierdził , że nazwa pochodzi od arabskiego określenia „ żurawia studziennego ” i zwracał uwagę na wielkie rzeki przepływające przez Irak .
We wczesnych wiekach islamu terminem „ Al-Irakani ” ( „ dwa Iraki ” ) określano miasta Al-Basra i Al-Kufa .
Zdaniem J. Głodka najbardziej uzasadnione jest wyprowadzenie nazwy kraju z języka akadyjskiego , w którym słowo `` irak '' oznacza „ kraj nasłoneczniony , słońca ” .
