Iran ( miejscowość )
Iran – miejscowość w Hiszpanii , w Katalonii , w prowincji Lleida , w comarce Alta Ribagorça , w gminie El Pont de Suert .
Według danych opublikowanych przez Institut d ’ Estadística de Catalunya w 2020 roku liczyła 6 mieszkańców – 2 mężczyzn i 4 kobiety .
Liczba mieszkańców w poprzednich latach : 8 ( 2008 ) , 9 ( 2009 ) , 8 ( 2014 ) , 8 ( 2015 ) , 6 ( 2019 ) .
