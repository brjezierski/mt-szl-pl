Irapuato
Irapuato – miasto w środkowym Meksyku , w stanie Guanajuato , na obszarze Mesy Centralnej , na wysokości 1800 metrów .
Około 440,1 tys .
mieszkańców .
Historia .
W okresie pre-hiszpańskim rejon zamieszkiwali Indianie z koczowniczego szczepu Czichimeków .
Później teren został podbity przez Indian Tarascan , którzy tworząc stałą siedzibę nadali miastu nazwę oznaczającą `` miejsce bagienne '' .
Obecna nazwa pochodzi o tego słowa i jest używana od założenia miasta przez kolonizatorów hiszpańskich w 1547 roku .
Prawa miejskie nadano w 1893 .
Gospodarka .
Irapuato jest ważnym ośrodkiem rolniczym z ogromnymi plantacjami truskawek .
Rozwinął się tu również przemysł , szczególnie chemiczny , młynarski i skórzano-obuwniczy .
W okolicach Irapuato wydobywa się srebro i złoto .
