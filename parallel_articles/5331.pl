Irena Kwiatkowska
Irena Kwiatkowska-Kielska ( ur .
17 września 1912 w Warszawie , zm .
3 marca 2011 w Konstancinie-Jeziornie ) – polska aktorka teatralna , filmowa i telewizyjna , artystka kabaretowa .
Życiorys .
Córka zecera Kryspina Stanisława Kwiatkowskiego ( 1876–1950 ) i jego żony Marianny z Barabaszów ( 1877–1945 ) .
Absolwentka ówcześnie żeńskiego Gimnazjum Państwowego im .
Klementyny Hoffmanowej w Warszawie .
W 1935 ukończyła studia na Wydziale Aktorskim Państwowego Instytutu Sztuki Teatralnej , pod kierunkiem m.in .
Aleksandra Zelwerowicza .
Zadebiutowała w teatrze Cyrulik Warszawski , gdzie została zaproszona przez Fryderyka Jarosyego .
Do wybuchu wojny grała w teatrach Powszechnym w Warszawie , Nowym w Poznaniu i Polskim w Katowicach .
Podczas okupacji niemieckiej pracowała w kuchni ZASP-u , podejmowała się też innych prac dorywczych .
Była żołnierką AK , uczestniczyła w powstaniu warszawskim jako łączniczka w sztabie grupy „ Północ ” ( ps .
„ Katarzyna ” ) , grała w teatrze powstańczym w szpitalach i piwnicach .
Po zakończeniu wojny , na prośbę Konstantego Ildefonsa Gałczyńskiego i Antoniego Bohdziewicza , wyjechała do Krakowa .
Tam występowała w kabarecie Siedem Kotów , gdzie wiersze , piosenki oraz skecze dla niej pisał Gałczyński .
Tutaj też w ramach Teatrzyku Zielona Gęś specjalnie dla Ireny Kwiatkowskiej stworzył postać Hermenegildy Kociubińskiej , `` poetki hermetyczno-sympatycznej '' .
