Irena Sendlerowa
Irena Stanisława Sendlerowa z domu Krzyżanowska , ps .
„ Jolanta ” ( ur .
15 lutego 1910 w Warszawie , zm .
12 maja 2008 tamże ) – polska działaczka społeczna i charytatywna , w czasie II wojny światowej kierowniczka referatu dziecięcego Rady Pomocy Żydom przy Delegaturze Rządu na Kraj ( „ Żegoty ” ) .
Sprawiedliwa wśród Narodów Świata , dama Orderu Orła Białego i Orderu Uśmiechu .
Od początku okupacji niemieckiej zaangażowała się w niesienie pomocy Żydom .
W październiku 1943 została aresztowana przez Gestapo , jednak „ Żegocie ” udało się ją uwolnić .
W czasie powstania warszawskiego była sanitariuszką w jednym z powstańczych punktów sanitarnych na Mokotowie .
Po wojnie pracowała w opiece społecznej , administracji państwowej i średnim szkolnictwie medycznym .
Przez długie lata jej działalność podczas okupacji niemieckiej była nieznana ; została spopularyzowana przez grupę teatralną z amerykańskiej szkoły średniej około roku 2000 .
W latach 2007 i 2008 jej kandydatura została zgłoszona do Pokojowej Nagrody Nobla .
Życiorys .
Młodość .
Ojciec , Stanisław Krzyżanowski ( 1875–1917 ) , syn Jana i Marianny z domu Krajewskiej , był lekarzem .
Matka , Janina Karolina Krzyżanowska z domu Grzybowska ( 1882–1944 ) , była córką Franciszka Ksawerego i Konstancji z domu Zakrzewskiej .
Ojciec należał do Polskiej Partii Socjalistycznej , wziął udział w rewolucji 1905 roku .
Z powodu zaangażowania politycznego miał kłopoty z ukończeniem studiów medycznych na Cesarskim Uniwersytecie Warszawskim ( ostatecznie studia ukończył w 1908 w Charkowie ) .
W 1909 wraz z nowo poślubioną żoną wrócił do Warszawy i podjął pracę w szpitalu św .
Ducha .
Irena Sendlerowa była jedynaczką .
W wieku dwóch lat zachorowała na koklusz , co skłoniło Krzyżanowskich do przeprowadzenia się z Warszawy do mającego status podmiejskiego uzdrowiska Otwocka .
Tam jej ojciec rozpoczął prywatną praktykę , lecząc głównie biedotę żydowską oraz chłopów .
Później prowadził tam sanatorium przeciwgruźlicze .
Był zaangażowany w działalność społeczną .
Atmosfera domu rodzinnego miała duży wpływ na działalność Ireny Sendlerowej w czasie II wojny światowej .
W Otwocku , bawiąc się z żydowskimi rówieśnikami , nauczyła się języka jidysz .
Odwiedzała również Tarczyn , gdzie mieszkali jej dziadkowie ze strony ojca .
W 1917 Stanisław Krzyżanowski zmarł na tyfus plamisty , którym zaraził się od swoich pacjentów .
W 1920 Janina Krzyżanowska przeprowadziła się z córką do Tarczyna , a następnie do Piotrkowa Trybunalskiego , gdzie mieszkała jej rodzina .
Po zdaniu egzaminów Irena rozpoczęła naukę w trzeciej klasie Gimnazjum Heleny Trzcińskiej .
W szkole średniej działała w harcerstwie .
Tam poznała swego przyszłego pierwszego męża , Mieczysława Sendlera .
W 1927 , po zdaniu matury , wyjechała do Warszawy .
W stolicy rozpoczęła studia na Wydziale Prawa Uniwersytetu Warszawskiego .
Po dwóch latach przeniosła się na Wydział Humanistyczny i rozpoczęła studia o specjalności polonistyka .
Odbyła praktykę pedagogiczną w filii Domu Sierot Janusza Korczaka „ Różyczka ” w Wawrze .
Przerwała studia w 1932 i wznowiła je , nie rezygnując z pracy zawodowej , w 1937 .
W latach 1928–1932 należała do lewicowego Związku Polskiej Młodzieży Demokratycznej .
W czasie zajść antysemickich , do których w latach 30. dochodziło na Uniwersytecie Warszawskim , stawała w obronie swoich żydowskich koleżanek .
Po wydaniu w październiku 1937 przez rektora Włodzimierza Antoniewicza zarządzenia wprowadzającego getto ławkowe siadała z Żydami w czasie zajęć .
Stała się przez to celem ataków ze strony studentów należących do ONR .
Przygotowywała pracę magisterską u Wacława Borowego pt .
`` Dziesięciolecie twórczości Elizy Orzeszkowej '' .
W czerwcu 1939 uzyskała absolutorium .
Złożyła pracę magisterską , ale nie przystąpiła do egzaminu magisterskiego ( prawdopodobnie zamierzała go zdać jesienią 1939 ) .
W 1932 podjęła pracę w Sekcji Opieki nad Matką i Dzieckiem Obywatelskiego Komitetu Pomocy Społecznej Wolnej Wszechnicy Polskiej , kierowanej przez Helenę Radlińską .
Poradnia m.in .
zapewniała opiekę prawną matkom z nieślubnymi dziećmi , przyznawała bezpłatne obiady , mleko dla dzieci oraz wyszukiwała lekarzy gotowych leczyć kobiety za darmo .
W 1934 Irena Sendlerowa opublikowała dwa teksty : `` O dziecko nieślubne '' i `` Matka nieślubna w poradni prawniczej '' .
Pracowała także w Poradni Prawnej Komitetu , której pracownicy m.in .
bronili bezrobotnych przed eksmisjami .
W 1935 instytucja została rozwiązana , a większość jej pracowników zatrudniona w Zarządzie Miejskim m.st .
Warszawy .
Irena Sendlerowa trafiła do Wydziału Opieki Społecznej i Zdrowia Publicznego .
Jako urzędniczka magistratu pracowała m.in .
w VI Ośrodku Opieki i Zdrowia przy ul .
Siedzibnej 25 ( tam opiekowała się m.in .
mieszkańcami osiedla dla bezrobotnych i bezdomnych na Annopolu ) , a później w siedzibie Wydziału przy ul .
Złotej 74 , gdzie zajmowała się szkoleniami .
