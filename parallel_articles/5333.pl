Irkuck
Irkuck ( , `` Irkutsk '' ) – miasto w Rosji , stolica obwodu irkuckiego .
Główny ośrodek gospodarczy , kulturalny ( muzea ) i naukowy ( uniwersytet zał .
1918 ) Syberii Wschodniej z rozwiniętym przemysłem maszynowym ( jedne z największych w Rosji zakłady lotnicze „ Irkut ” ) , aluminiowym , materiałów budowlanych , drzewnym , skórzanym i spożywczym .
W mieście znajduje się elektrownia wodna o mocy 660 MW .
Położenie .
Główna część miasta leży na prawym brzegu rzeki Angara , która jest dopływem Jeniseju .
Po drugiej stronie rzeki leży dzielnica Głazkowo .
Rzekę , która ma do 579 m szerokości , można przekroczyć trzema mostami oraz drogą biegnącą po koronie zapory Zbiornika Irkuckiego .
Niewielka rzeka Irkut , od której miasto wzięło swoją nazwę , wpada jako lewostronny dopływ Angary na wysokości centrum .
Historia .
Port nad rzeką Angarą .
Założony w 1661 jako Irkuckie Zimowisko .
Prawa miejskie osada posiada od 1686 roku .
Ośrodek administracyjny – wojsk dla kolonizacji Syberii .
