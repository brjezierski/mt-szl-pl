Mięso dusi się z ziemniakami , cebulą , marchewką , porem i kaszą jęczmienną .
Od XIX wieku ziemniaki są podstawą diety Irlandczyków .
Sport i kultura .
W niektórych dyscyplinach , głównie w rugby , reprezentacja narodowa gra w pucharze 6 narodów i odnosi w nim sukcesy .
Mieszkańcy interesują się przede wszystkim sportami narodowymi : hurlingiem , futbolem gaelickim , rugby , krykietem oraz wyścigami konnymi i wyścigami psów .
Z Irlandii pochodzi Conor McGregor – były mistrz kategorii lekkiej i piórkowej UFC .
Irlandia siedmiokrotnie wygrała Konkurs Piosenki Eurowizji , co czyni ją najbardziej utytułowanym uczestnikiem konkursu .
