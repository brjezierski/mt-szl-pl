Irlandia
Irlandia ( , ) , oficjalnie Republika Irlandii ( irl .
`` Poblacht na hÉireann '' , ang .
`` Republic of Ireland '' ) – państwo wyspiarskie w zachodniej części Europy , zajmujące większość terytorium wyspy Irlandia , członek Unii Europejskiej .
Nazwa .
Pochodzenie nazwy .
Historyczną , łacińską nazwą Irlandii , nadaną przez Rzymian , była `` Hibernia '' , pochodząca od greckiego określenia Ἰουερνία ( `` Iuernia '' ) .
Spotykana jest także nazwa `` Ἰέρνη '' ( `` Iernē '' ) .
Nazwa rzymska została „ przerobiona ” na `` Hibernia '' ze względu na swoje podobieństwo do wyrazu ( łac . )
`` hibernus '' – „ zimowy ” .
Staroirlandzkie zapożyczenie nazwy jako `` īweriū '' / `` īwerion '' , po pewnym czasie przeistoczyło się we współczesną nazwę `` Éire '' .
Nazwa współczesna .
Artykuł 4. konstytucji Irlandii z 1937 roku stanowi , iż oficjalną nazwą państwa jest `` Éire '' ( w języku irlandzkim ) oraz `` Ireland '' ( w języku angielskim ) .
Oba warianty językowe nazwy są używane podczas podpisywania umów międzynarodowych i traktatów stowarzyszeniowych .
Stosowanie jednej nazwy dla określenia wyspy , jak i państwa , miało na celu podkreślenie integralności Irlandii Północnej i pozostałej części wyspy , jako jednego organizmu państwowego .
Państwa stowarzyszone we Wspólnocie Narodów , włączając w to Zjednoczone Królestwo , dla określenia Irlandii jako państwa używają nazwy `` Éire '' .
Podobnie w państwach europejskich , przesyłki pocztowe adresowane są do Irlandii z dodatkowym dopiskiem `` Éire '' .
Według przepisów z 1947 roku w pismach urzędowych dotyczących Irlandii jako państwa powinno stosować się nazwę `` Éire '' .
