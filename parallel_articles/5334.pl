Emisja gazów cieplarnianych .
Emisja równoważnika dwutlenku węgla z Irlandii wyniosła w 1990 roku 57,500 Mt , z czego 32,851 Mt stanowił dwutlenek węgla .
W przeliczeniu na mieszkańca emisja wyniosła wówczas 9,204 t dwutlenku węgla , a w przeliczeniu na 1000 dolarów PKB 432 kg .
Następnie emisje wahały się , osiągając maksymalne wartości w pierwszej dekadzie XXI w. W 2018 emisja dwutlenku węgla pochodzenia kopalnego wyniosła 37,206 Mt , a w przeliczeniu na mieszkańca 7,745 t i w przeliczeniu na 1000 dolarów PKB 10982 kg .
Źródła emisji w latach 90. były zróżnicowane , z największym udziałem i energetyki i emisji z budynków , ale z czasem zaczął rosnąć udział emisji z transportu .
Głównym gazem cieplarnianym emitowanym z Irlandii jest dwutlenek węgla , emisja metanu jest na drugim miejscu , a na dalszych miejscach są emisje podtlenku azotu i gazów fluorowanych .
