Podział administracyjny .
Od końca XVI w. wyspa Irlandia tradycyjnie dzielona była na 4 historyczne prowincje , a te z kolei dzielono na 32 hrabstwa .
Dwa historyczne hrabstwa : Desmond oraz Coleraine obecnie nie istnieją , a niektóre inne zmieniły nazwy bądź granice .
