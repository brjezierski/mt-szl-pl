12 % mieszkańców to cudzoziemcy .
W spisie powszechnym w 2011 roku zanotowano ogólny wzrost populacji w okresie 2006–2011 o 8,1 % .
Największy wzrost miał miejsce w hrabstwie Laois i wynosił 20 % .
Kolejnymi hrabstwami , gdzie wzrost wynosił średnio 13 % , były : Cavan , Fingal , Longford , Meath i Kildare .
W latach 2006–2011 , w porównaniu do lat 2002–2006 , Irlandia zanotowała ogólny spadek migracji ludności , chociaż w pierwszych latach tego okresu był to jeszcze wzrost emigracji .
Średnia liczba ludności emigrującej do Irlandii w tym okresie wynosiła 23 730 osób w skali roku .
Największy wzrost emigrantów zanotowano w hrabstwie Laois , zaś największy odsetek ludności imigrującej – w Limerick .
W okresie tym zanotowano również odpływ ludności z centrum Dublina do hrabstw ościennych : Dun Laoghaire-Rathdown , Fingal i Dublin Południowy oraz do hrabstwa Meath i Kildare .
Polonia w Irlandii .
Polonia w Irlandii wykształciła się głównie w momencie wstąpienia Polski do Unii Europejskiej w 2004 roku .
Według spisu ludności w 2011 roku liczba osób polskiego pochodzenia mieszkających w Irlandii wynosiła 122 585 osób .
Ustrój polityczny .
Irlandia to demokracja parlamentarna .
Jej system prawny jest oparty na prawie precedensowym i ustawodawstwie uchwalanym przez parlament zgodnie z konstytucją .
Przepisy Unii Europejskiej mają moc prawną w Irlandii .
Konstytucja irlandzka , przyjęta 29 grudnia 1937 , opisuje formę rządu oraz definiuje władzę i funkcje Prezydenta , obu izb parlamentu ( `` Oireachtas '' ) oraz rządu .
Definiuje także strukturę i władzę sądu oraz opisuje podstawowe prawa obywatelskie .
Akt Republiki Irlandii z 1948 zerwał ostatnie formalne związki z Wielką Brytanią .
Irlandia odzyskała niepodległość w 1921 .
Prezydent jest głową państwa i jest wybierany ( wybierana ) w wyborach bezpośrednich na kadencję , która trwa 7 lat .
Ta sama osoba może być prezydentem tylko dwukrotnie .
Bierne prawo wyborcze przysługuje obywatelom Irlandii , którzy ukończyli 35 lat .
Od 2011 prezydentem Irlandii jest Michael D. Higgins .
Obowiązkiem prezydenta jest stanie na straży konstytucji oraz oficjalne reprezentowanie narodu .
Niemal wszystkie uprawnienia przyznawane mu przez konstytucję wykonuje on za radą rządu .
Taoiseach ( premier ) stoi na czele rządu i jest mianowany przez prezydenta na wniosek Dáil Éireann ( izba niższa parlamentu ) .
Taoiseach nominuje innych członków rządu ( ogółem nie mniej niż siedmiu i nie więcej niż piętnastu ) , których musi zaakceptować Dáil Éireann .
Nominuje także jednego członka rządu , zwanego Tánaiste , który pełni funkcję wicepremiera .
Od 2020 premierem Irlandii jest Micheál Martin .
