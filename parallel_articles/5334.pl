Religia .
W Irlandii oficjalnie panuje rozdział państwa od Kościoła , a konstytucja zabrania promowania jednej religii .
Irlandzki Kościół rzymskokatolicki pozostaje największą wspólnotą religijną w Irlandii z liczbą 3,73 mln wiernych .
W spisie powszechnym z 2016 roku 78,3 % populacji zadeklarowało katolicyzm , co było spadkiem o 10 % w stosunku do roku 2002 .
W liczbach bezwzględnych liczba katolików wzrosła jednak o ponad 266 tysięcy .
Spowodowane to było przede wszystkim napływem imigrantów z Polski i Litwy .
Drugą grupą liczoną według wyznania są osoby określające się w spisie jako „ bez religii ” .
W okresie 2002-2016 liczba osób bez wyznania i niezdeklarowanych wzrosła z 3,6 % do 10,1 % .
Największy odsetek tych osób znajduje się na wschodzie ( głównie w Dublinie ) i północy kraju ( nie licząc Irlandii Północnej ) .
