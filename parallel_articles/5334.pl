Rozwody , kontakty homoseksualne , aborcja , antykoncepcja i edukacja seksualna były prawnie zakazane .
Na skutek dynamicznego rozwoju gospodarczego , podniesieniu się standardu życia mieszkańców kraju , upowszechnienia się konsumpcjonizmu i technologii informacyjnej Kościół i religia przestały być jedynym drogowskazem .
Irlandzkie społeczeństwo zaczęło się wewnętrznie różnicować , a religia zaczęła być wypierana przez konsumpcyjny styl życia , prywatyzację wizji świata i nowe światopoglądy .
Dodatkowo na skutek ujawnionych skandali prawnych i finansowych oraz przestępstw seksualnych popełnianych przez niektórych katolickich duchownych ( w tym biskupów ) , jak również powszechne ukrywanie tych przypadków przez hierarchię autorytet Kościoła w irlandzkim społeczeństwie został bardzo mocno zachwiany .
Irlandczycy zaczęli na niespotykaną wcześniej skalę odwracać się od Kościoła i podnosić głosy sprzeciwu i ostrej krytyki na próby przypomnienia wiernym o katolickiej etyce seksualnej i moralności .
Ponad 2/3 Irlandczyków jest zdania , że Kościół zawiódł i utracił autorytet moralny .
Kryzys zaufania do Kościoła w irlandzkim społeczeństwie jest dziś bardzo duży , co w znacznym stopniu wpłynęło na kryzys powołań kapłańskich i rezygnację z posługi wielu duchownych .
W ciągu ostatnich kilkunastu lat zamknięto prawie wszystkie seminaria duchowne , zostało tylko jedno w Maynooth .
W połowie lat 60. w seminarium tym wyświęcano ponad 400 nowych księży rocznie .
W 2001 roku już tylko 30 , a w 2007 roku zaledwie 9 w skali całego kraju .
Kryzys ten w jeszcze większym stopniu dotyka żeńskie zgromadzenie zakonne .
W 2007 śluby złożyły zaledwie 2 kobiety , podczas gdy w tym samym czasie liczba sióstr zakonnych zmniejszyła się o 228 .
Najbardziej widocznym przejawem kryzysu zaufania do Kościoła jest spadek uczestnictwa w mszach niedzielnych z 90 % w latach 70. , 81 % w 1990 roku do 60 % w 1996 roku , 44–48 % w latach 2005–2006 i 35 % w 2012 .
