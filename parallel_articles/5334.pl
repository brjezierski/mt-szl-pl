Transport .
Strategiczne znaczenie ma transport drogowy .
Irlandia ma gęstą sieć dróg , które często nie są jednak w najlepszym stanie , a ich całkowita długość wynosi 92 327 km , z czego ok. 1017 km ma standard autostrady ( 2018 ) .
Większość autostrad jest płatna .
W Irlandii obowiązuje ruch lewostronny .
Oprócz licznych przewoźników prywatnych transportem autobusowym w kraju zajmuje się subsydiowana przez państwo firma Bus Éireann .
Firma ta jest odpowiedzialna zarówno za transport międzymiastowy , jak i za transport lokalny ( komunikacja miejska w kilku większych miastach jednakże z wyłączeniem Dublina ) .
Sieć kolejowa ma dł .
1944 km i jej wyłącznym operatorem jest firma Iarnród Éireann .
W ostatnich latach rozwijają się także inne formy transportu szynowego , czyli tramwaj Luas w Dublinie oraz planowane metro .
Najważniejszymi portami handlowymi są Dublin i Cork , a morska żegluga handlowa liczy 150 statków .
Największym portem morskim jest Dublin , zaś przewozy transatlantyckie obejmują przede wszystkim Cork .
Przystanie promowe w Dublinie , Dún Laoghaire oraz Rosslare zapewniają połączenia z głównymi portami Wielkiej Brytanii .
Zbudowana w XVIII i XIX w. , a następnie modernizowana sieć kanałów służy przede wszystkim do przewozu towarów , lecz ma także znaczenie turystyczne .
Międzynarodowe porty lotnicze w Dublinie , Cork , Knock oraz Shannon są znane także jako strefy wolnocłowe .
Irlandia ma 40 lotnisk krajowych i międzynarodowych .
