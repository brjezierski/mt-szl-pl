Rzymianie nie podjęli próby podboju wyspy , mimo bliskości Brytanii .
Od V wieku naszej ery wyspa była chrystianizowana za sprawą św .
Patryka , zaś od VIII wieku rozpoczęła się inwazja wikingów , która w 1014 roku ostatecznie została odparta za sprawą wojsk ówczesnego króla Briana Śmiałego .
Od 1169 roku rozpoczęły się wyprawy anglo-normandzkie , rozpoczynając tym samym wielowiekową dominację angielską nad wyspą .
W okresie tym miały miejsce wielokrotnie różnego rodzaju powstania i wojny przeciwko dominacji , jak irlandzka wojna dziewięcioletnia w 1594 r. , bitwa nad rzeką Boyne w 1690 r. czy też rewolucja irlandzka w 1798 roku .
Jednocześnie strona Anglii nie trwała bezczynnie .
Już w 1175 r. Anglia umocniła wpływy poprzez Traktat windsorski , w 1605 r. rozpoczęła wysiedlenia zwane plantacją Ulsteru , natomiast na podstawie aktu z 1800 roku połączyła się z Irlandią w jedno państwo , tworząc Zjednoczone Królestwo Wielkiej Brytanii i Irlandii .
Wielki głód miał miejsce w Irlandii w latach 1845–1849 .
Populacja Irlandii zmniejszyła się wówczas o 20 % , doszło również do wielkiej fali emigracji – Irlandię opuściły 2 000 000 osób .
Koniec angielskiej dominacji miał miejsce w początkach XX wieku , najpierw poprzez rozpoczęcie powstania wielkanocnego w 1916 roku oraz poprzez podpisanie Traktatu angielsko-irlandzkiego w 1921 .
W tym okresie powstał również pierwszy rząd irlandzki z Éamonem de Valerą na czele , co spowodowało wybuch irlandzkiej wojny domowej .
Ostatecznie w 1937 w życie weszła Konstytucja Irlandii , na mocy której utworzono państwo Irlandię , która to obowiązuje do dnia dzisiejszego .
W 1955 roku Irlandia stała się członkiem ONZ , zaś w 1973 wstąpiła do Wspólnoty Europejskiej .
W 1969 roku swój początek miał polityczno-etniczny konflikt w Irlandii Północnej , zakończony w 1998 roku podpisaniem porozumienia wielkopiątkowego pomiędzy Irlandią i Wielką Brytanią .
Demografia .
Między rokiem 1841 a 1851 liczba ludności spadła z 8,2 miliona do 6,5 miliona osób .
Przyczyną gwałtownego spadku liczby ludności była śmierć głodowa i emigracja ( przeważnie do Wielkiej Brytanii i USA ) .
Aktualnie poza granicami wyspy mieszka od 12 do 16 milionów osób pochodzenia irlandzkiego ( 13 mln w Stanach Zjednoczonych , a 1 mln w Kanadzie ) .
W 2007 roku , był to kraj o najszybciej rosnącej populacji w Europie .
Tempo wzrostu w 2006 wynosiło 2,5 % , a trzy poprzednie lata kształtowały się na poziomie 2 % .
W 2011 zanotowano rekordową liczbę 70 tys .
