Do zapisu języka irlandzkiego używa się alfabetu łacińskiego .
Do niedawna w powszechnym użyciu był dość odmienny od powszechnie przyjętego w piśmie łacińskim krój liter , nazywany alfabetem irlandzkim , który także obecnie jest używany , aczkolwiek sporadycznie .
We wczesnym średniowieczu do zapisu języka ( staro ) irlandzkiego używane było pismo ogamiczne .
Najstarsze inskrypcje zapisane właśnie tym pismem pochodzą z V wieku .
Przodkiem języka irlandzkiego jest język staroirlandzki , który dał początek także szkockiemu gaelickiemu oraz językowi manx .
Shelta to język mieszany powstały w wyniku intensywnego angielsko-irlandzkiego kontaktu językowego .
