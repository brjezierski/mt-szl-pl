Język irlandzki
Język irlandzki , język iryjski ( `` Gaeilge '' , wym . )
– język z grupy goidelskiej języków celtyckich , język narodowy Irlandczyków .
Język urzędowy Republiki Irlandii ( obok angielskiego ) .
W 2011 około 1,77 miliona mieszkańców Irlandii zadeklarowało , że posiada jakąkolwiek znajomość swojego języka narodowego , wyniesioną głównie ze szkolnych lekcji .
