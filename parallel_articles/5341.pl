Irpień ( rzeka )
Irpień ( , `` Irpiń '' ) , – rzeka na Ukrainie , prawy dopływ Dniepru .
Ma 162 km w długości i płynie przez miasto Irpień .
Wpada obecnie nie bezpośrednio do Dniepru , a do Zbiornika Kijowskiego , utworzonego na potrzeby kijowskiej elektrowni wodnej .
W ten sposób wody Irpienia pompowane są na 6,57 metrów powyżej poziomu naturalnego .
Rzeka wymieniana jest w kronikach w związku z kilkoma wydarzeniami historycznymi , takimi jak np .
bitwa nad Irpieniem ( 1321 ) , w wyniku której Giedymin , pobiwszy Stanisława kijowskiego , stał się władcą późniejszej Ukrainy Nadnieprzańskiej .
Od zawarcia traktatu Grzymułtowskiego ( 1686 ) do II rozbioru Polski ( 1793 ) Irpieniem biegł odcinek granicy polsko-rosyjskiej .
