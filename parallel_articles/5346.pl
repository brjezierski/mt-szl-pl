Isaac Asimov
Isaac Asimov ( wym .
, urodzony jako Isaak Judowicz Ozimow ( ros .
Исаак Юдович Озимов ) ; ur .
2 stycznia 1920 w Pietrowiczach , Rosyjska Federacyjna SRR , zm .
6 kwietnia 1992 w Nowym Jorku ) – amerykański pisarz i profesor biochemii pochodzenia żydowskiego .
Największą popularność zdobył jako autor science fiction .
Największy sukces osiągnął jako autor książek fantastycznonaukowych oraz popularnonaukowych .
Większość prac Asimova z zakresu popularyzacji nauki wyjaśnia koncepcje naukowe w sposób historyczny , cofając się w czasie w miarę możliwości jak najdalej , do samych początków rozwoju danej dziedziny .
Często podaje narodowości , daty urodzenia i śmierci wspominanych przez siebie naukowców , jak również etymologię i sposoby wymawiania terminów technicznych .
Jako przykłady można wymienić `` Guide to Science , '' trzytomowy zbiór `` Understanding Physics '' oraz `` Asimov ’ s Chronology of Science and Discovery . ''
Asimov , jako autor i redaktor napisał ponad 500 książek , oraz w przybliżeniu 9000 listów i kartek pocztowych , był jednym z najbardziej płodnych pisarzy wszech czasów .
