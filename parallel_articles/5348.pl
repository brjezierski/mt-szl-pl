Ise ( zatoka )
– zatoka w południowej części regionu Chūbu , w Japonii , połączona z Oceanem Spokojnym cieśniną Irago .
Wejście do zatoki otaczają półwyspy : Shima od strony prefektury Mie i Atsumi od strony prefektury Aichi .
We wschodniej części zatoka Ise jest połączona z zatoką Mikawa .
U wybrzeży zatoki Ise są położone m.in .
porty Nagoja , Yokkaichi .
Region wokół zatoki jest silnie uprzemysłowiony .
Ważne miasta nad zatoką , oprócz wymienionych portów , to : Kuwana , Tsu , Ise w ( prefekturze Mie ) oraz Tokoname ( w prefekturze Aichi ) .
Zatoka jest znana z hodowli pereł , główny ośrodek jest w mieście Toba .
