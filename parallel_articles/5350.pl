Isfahan ( ostan )
Isfahan ( translit .
`` Ostān-e Eşfahān '' , transkryp .
`` Ostan-e Esfahan '' ) – ostan w środkowym Iranie .
Stolicą jest Isfahan .
Gospodarka .
W ostanie uprawia się ryż , pszenicę , bawełnę , warzywa , drzewa owocowe oraz hoduje się owce .
W regionie wydobywa się rudy miedzi , antymonu , cynku , ołowiu , niklu oraz żelaza .
W ostanie rozwinął się przemysł dywanowy oraz metalowy .
