Isfara ( rzeka )
Isfara – rzeka w Kirgistanie , Tadżykistanie i Uzbekistanie .
Jej długość wynosi 130 km , a dorzecze zajmuje powierzchnię 3240 km²
Wypływa z lodowców w Górach Turkiestańskich .
U wejścia do Kotliny Fergańskiej tworzy duży stożek napływowy .
W dolnym biegu rzeka rozlewa się wieloma kanałami , które połączone są z Wielkim Kanałem Fergańskim .
Reżim lodowcowo-śnieżny .
Wykorzystywana do nawadniania .
