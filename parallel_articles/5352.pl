Islam
Islam ( , `` al-islām '' ) – religia monoteistyczna , na początku XXI w. druga na świecie pod względem liczby wyznawców po chrześcijaństwie .
Świętą księgą islamu jest Koran , a zawarte w nim objawienie ma stanowić ostateczne i niezmienne przesłanie Allaha do ludzi .
Islam bywa określany jako mahometanizm .
Słowo `` islam '' w języku arabskim oznacza `` poddanie się woli Boga '' .
Islam ma wspólne korzenie z judaizmem i chrześcijaństwem .
Zgodnie z nauką proroka Mahometa objawienie przekazane Żydom i chrześcijanom przez Abrahama , Mojżesza i Jezusa powinno zostać ponowione i uzupełnione .
