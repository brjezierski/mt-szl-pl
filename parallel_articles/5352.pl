Przesłanie Mahometa początkowo głoszone było wśród przedstawicieli plemion arabskich , z czasem jednak rozpowszechniło się na całym świecie .
Obecnie Arabowie stanowią około 20–25 % wyznawców islamu , większość muzułmanów żyje natomiast w krajach Azji , najliczniej w Indonezji , Indiach , Pakistanie i Bangladeszu .
Tradycyjnie islam dzieli się na trzy główne odłamy : sunnicki ( 85–90 % ) , szyicki ( 10–15 % ) i charydżycki ( & lt ; 1 % ) .
Podział taki nie uwzględnia jednak głębokich różnic wyznaniowych , teologicznych , historycznych i politycznych wśród wielomilionowych grup takich jak nurt sufizmu , islam bezdenominacyjny ( wyznawcy identyfikujący się jako `` tylko muzułmanie '' ) , odłam ahmadijja , czy fundamentalistyczny salafizm .
Symbole islamu .
Właściwym symbolem islamu jest szahada ( wyznanie wiary ) napisana czarnymi literami na białym tle lub białymi literami na czarnym tle .
