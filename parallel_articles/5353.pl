Islamabad
Islamabad ( urdu : `` اسلام آباد '' ) – stolica Pakistanu od 1966 roku , położona w północno-wschodniej części kraju .
Według spisu powszechnego z 2017 roku , Islamabad jest dziewiątym miastem Pakistanu pod względem liczby ludności .
Historia .
Miasto zostało zbudowane od podstaw w latach 60 .
XX wieku według całościowego planu architektonicznego greckiego architekta-planisty Konstandinosa Doksiadisa , zgodnie z jego teorią ekistyki .
Celem było zastąpienie Karaczi jako stolicy kraju .
Przeniesienie miasta stołecznego ówczesny prezydent Muhammad Ayub Khan tłumaczył chęcią bardziej równomiernego rozłożenia rozwoju Pakistanu , który był wtedy skupiony w rejonie Karaczi .
O budowie stolicy zdecydowało również położenie geograficzne .
Od 1966 roku Islamabad jest miastem stołecznym i siedzibą władz politycznych kraju .
Geografia i klimat .
Miasto położone na zachodnim przedgórzu Himalajów , jest otoczone górami – od północy graniczy z pasmem Margalla Hills ( i parkiem narodowym o takiej samej nazwie ) .
