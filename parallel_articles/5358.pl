Ismailia ( muhafaza )
Ismailia ( arab .
الإسماعيلية ) – prowincja gubernatorska ( muhafaza ) w Egipcie , w północnej części .
Zajmuje powierzchnię 5066,97 km2 .
Przez środek muhafazy przebiega Kanał Sueski .
Stolicą administracyjną jest Ismailia .
Według spisu powszechnego w listopadzie 2006 roku populacja muhafazy liczyła 953 006 mieszkańców , natomiast według szacunków 1 stycznia 2015 roku zamieszkiwało ją 1 178 641 osób .
