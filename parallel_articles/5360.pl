Według danych na rok 2012 gminę zamieszkiwało 65 322 osób , a gęstość zaludnienia wynosiła 15 370 osób/km² .
Wśród 1287 gmin regionu Île-de-France Issy-les-Moulineaux plasuje się na 734. miejscu pod względem powierzchni .
W miejscowości znajduje się główna siedziba telewizji Eurosport oraz France 24 .
