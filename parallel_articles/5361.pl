Istebna
Istebna – wieś w Polsce , położona w województwie śląskim , w powiecie cieszyńskim , siedziba gminy Istebna .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Istebna .
W latach 1975–1998 wieś położona była w województwie bielskim .
Wieś leży w historycznych granicach regionu Śląska Cieszyńskiego .
Powierzchnia sołectwa wynosi 4741 ha , a liczba ludności 5142 osoby , co daje gęstość zaludnienia równą 108,4 os./km² .
Miejscowość turystyczna położona w Beskidzie Śląskim w pobliżu granicy z Czechami i Słowacją .
Obejmuje dolinę górnej Olzy i stoki otaczających ją wzniesień .
Centrum zabudowy na południowych zboczach Złotego Gronia leży na wysokości 580–630 m n.p.m .
Krajobraz miejscowości to głównie tereny górzyste z zalesieniami .
Wraz z Jaworzynką i Koniakowem tworzy tzw .
