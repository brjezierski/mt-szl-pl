Historia .
Pierwszą osadą na terenie Istebnej były Jasnowice , powstały one najprawdopodobniej w połowie XVI w. ; .
Pod koniec tego też wieku na stokach południowych Złotego Gronia pojawili się osadnicy , tworząc podstawy pod rozwój nowej wsi Istebna .
Jednak pierwsza wzmianka o niej pojawiła się w urbarzu księstwa cieszyńskiego dopiero w 1621 r. – spisano w nim wszystkich osadników oraz ich powinności , które miały obowiązywać od 1643 r. Dlatego lata 1621–1643 uznawane są za okres kształtowania się organizacji wsi .
