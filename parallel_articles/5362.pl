Sarajewo Wschodnie
Sarajewo Wschodnie ( cyr .
Источно Сарајево ) – dawna wschodnia część Sarajewa , leżąca w Bośni i Hercegowinie , w Republice Serbskiej .
Jej obszar podzielony jest pomiędzy gminy : Istočna Ilidža , Istočni Stari Grad i Istočno Novo Sarajevo .
Geografia .
Sarajewo Wschodnie leży w centralnym punkcie Bośni i Hercegowiny .
