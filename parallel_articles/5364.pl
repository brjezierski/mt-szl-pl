Iszym ( miasto )
Iszym ( ) — miasto w środkowo-południowej części Rosji , w obwodzie tiumeńskim .
Położone jest na lewym brzegu rzeki Iszym , 120 km od granicy z Kazachstanem .
12 km od miasta znajduje się zabytkowy las – Sosnowy Bór .
W mieście rozwinął się przemysł maszynowy , lekki oraz spożywczy .
Historia .
W 1687 roku na brzegu rzeki Iszym powstała Korkinska Słoboda .
Od 1721 roku odbywały się w niej targi `` Nikolskaja Jarmarka '' .
W 1782 roku Korkinskaja Słoboda , na życzenie Katarzyny II otrzymała nazwę Iszym .
W rotach aresztanckich w Iszymie przebywał m.in .
Tomasz Kiciński ( 1806-1882 ) , ppor .
powstania listopadowego .
W roku 1913 przez miejscowość przeprowadzona została kolej Tiumeń – Omsk .
