Język włoski
Język włoski ( wł .
, ) – język romański objęty statusem urzędowego we Włoszech , San Marino , Watykanie ( obok łaciny ) , Szwajcarii ( obok francuskiego , niemieckiego i romansz ) , Monako ( obok francuskiego i monegaskiego ) oraz na Istrii , należącej do Chorwacji i Słowenii .
W pewnym stopniu używany również we Francji ( zwłaszcza na Korsyce ) i na Malcie .
Można się nim także porozumieć w skupiskach emigracji włoskiej w Argentynie , Australii , Stanach Zjednoczonych i Tunezji oraz w pewnym niewielkim stopniu w Erytrei , byłej kolonii włoskiej ( przez pewien czas używano go też w Libii i Somalii , ale po wycofaniu się włoskich kolonialistów wyszedł tam z użycia ) .
Włoski jest silnie zróżnicowany dialektalnie , jego współczesna forma standardowa oparta jest na dialekcie toskańskim , znormalizowanym na przełomie XIII i XIV wieku przez Dantego Alighieri i wielu współczesnych mu twórców , takich jak Francesco Petrarca i Giovanni Boccaccio .
Język włoski funkcjonuje również w krajach , gdzie jest pozbawiony statusu oficjalnego .
