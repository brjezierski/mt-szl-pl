Ivan Gašparovič
Ivan Gašparovič ( ur .
27 marca 1941 w Poltárze ) – słowacki polityk , prawnik i nauczyciel akademicki , profesor prawa , przewodniczący Rady Narodowej , prezydent Słowacji od 15 czerwca 2004 do 15 czerwca 2014 .
Życiorys .
Urodził się w miejscowości Poltár , w pobliżu Łuczeńca .
Jego ojciec , Vladimir Gašparović , wyemigrował do Słowacji z Rijeki w Chorwacji pod koniec I wojny światowej i pracował jako nauczyciel w gimnazjum w Bratysławie .
W latach 1959–1964 odbył studia prawnicze na Wydziale Prawa Uniwersytetu Komeńskiego w Bratysławie .
Podjął pracę w prokuraturze w Martinie , a następnie w Bratysławie .
Był członkiem Komunistycznej Partii Czechosłowacji , deklarował poparcie dla reform Alexandra Dubčeka .
Został z wykluczony z partii komunistycznej po inwazji wojsk Układu Warszawskiego na Czechosłowację w 1968 .
Od 1968 do lipca 1990 był nauczycielem akademickim w instytucie prawa karnego , kryminologii i kryminologii na Wydziale Prawa Uniwersytetu Komeńskiego w Bratysławie .
W międzyczasie doktoryzował się i objął stanowisko profesora .
