Ivan Perišić
Ivan Perišić ( ur .
2 lutego 1989 w Splicie ) – chorwacki piłkarz , występujący na pozycji pomocnika w angielskim klubie Tottenham Hotspur oraz w reprezentacji Chorwacji .
Wicemistrz Świata 2018 i brązowy medalista Mistrzostw Świata 2022 .
Uczestnik Mistrzostw Europy 2012 , 2016 , 2020 , Mistrzostw Świata 2014 , 2018 i 2022 .
Zdobywca Ligi Mistrzów UEFA z Bayernem Monachium w sezonie 2019/2020
Kariera klubowa .
Karierę piłkarską rozpoczął jako junior w Hajduku Split .
W 2006 odszedł do juniorskiego zespołu francuskiego FC Sochaux-Montbéliard .
Na początku 2007 został włączony do jego rezerw , w których spędził 2 kolejne lata .
W styczniu 2009 został wypożyczony do belgijskiego KSV Roeselare .
W Eerste klasse zadebiutował 17 stycznia 2009 w przegranym 1:2 pojedynku z KV Kortrijk .
