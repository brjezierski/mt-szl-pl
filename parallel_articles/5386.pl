Izrael ( hebr .
ישראל , `` Jisra'el '' ) – imię hebrajskie o niejasnym już dziś znaczeniu .
Zgodnie z biblijną przypowieścią , powstało z połączenia słów oznaczających „ będzie się zmagał z Bogiem '' ( `` לשרות '' , `` lisrot '' – „ zmagać się ” , `` אל '' , `` El '' – „ Bóg '' ) .
Według tradycji po raz pierwszy nadano je patriarsze Jakubowi , który po walce z posłańcem Boga lub samym Bogiem usłyszał : „ Odtąd nie będziesz nazywał się Jakub , ale Izrael , bo walczyłeś z Bogiem i z ludźmi i zwyciężyłeś ” ( Księga Rodzaju 32,29 ) .
