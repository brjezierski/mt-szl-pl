Iława
Iława ( niem .
, prus . )
– miasto w województwie warmińsko-mazurskim , siedziba powiatu iławskiego .
W latach 1975–1998 miejscowość należała administracyjnie do województwa olsztyńskiego .
Iława jest ośrodkiem wypoczynkowym , paraolimpijskim i turystycznym .
W lesie pod Iławą znajdują się dwa ośrodki wczasowe Telewizji Polskiej ( Sarnówek i Tłokowisko ) .
Z Iławy można dopłynąć do Morza Bałtyckiego poprzez jezioro Jeziorak i unikatowy w skali światowej Kanał Elbląski .
Nad jeziorem Silm znajduje się jeden z kilku na świecie ośrodków szkolenia kapitanów żeglugi morskiej i pilotów portowych .
Przed II wojną światową miejscowość nazywana była „ Perłą Oberlandu ” .
Miasto nazywane jest letnią stolicą jazzu tradycyjnego ze względu na najstarszy festiwal tego gatunku muzyki w Europie – Old Jazz Meeting „ Złota Tarka ” .
W Iławie znajduje się Papieska Kalwaria Pojezierza Iławskiego , której stacje Drogi Krzyżowej w kształcie kajaków upamiętniają dwukrotny pobyt Karola Wojtyły nad Jeziorakiem , po którym przyszły Papież pływał kajakiem z młodzieżą .
