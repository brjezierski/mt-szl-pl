Iłowajśk
Iłowajśk ( ) – miasto na Ukrainie w obwodzie donieckim , ważny węzeł komunikacyjny obwodu donieckiego .
Historia .
W kwietniu 2014 roku miasto opanowali separatyści tzw .
Donieckiej Republiki Ludowej .
Akcja wojsk ukraińskich doprowadziła w połowie sierpnia do odzyskania kontroli nad częścią miasta , ale pod koniec tego miesiąca ciężkie walki doprowadziły do zamknięcia ich w tzw .
„ kotle ” oraz odcięcia od dostaw broni , amunicji i zaopatrzenia .
Po przegranej wojsk ukraińskich doszło do ponownego przejęcia miasta przez separatystów .
