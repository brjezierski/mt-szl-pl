Iłowo-Osada
Iłowo-Osada ( daw .
`` Iłowo '' , niem .
`` Illowo '' ) – wieś w Polsce położona na Wzniesieniach Mławskich , w województwie warmińsko-mazurskim , w powiecie działdowskim , w gminie Iłowo-Osada przy drodze wojewódzkiej nr 544 między Mławą a Działdowem .
Miejscowość leży przy linii kolejowej nr 9 Warszawa – Gdańsk .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Iłowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa ciechanowskiego .
Miejscowość jest siedzibą gminy Iłowo-Osada .
