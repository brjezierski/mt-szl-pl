Iłża
( , , ) – miasto leżące na południowym krańcu województwa mazowieckiego , w powiecie radomskim , siedziba gminy miejsko-wiejskiej Iłża .
Położone na północnym krańcu Wyżyny Kieleckiej , historycznie w Małopolsce , w ziemi radomskiej .
Na terenie miasta i gminy Iłża znajduje się podstrefa Specjalnej Strefy Ekonomicznej Starachowice , obejmująca obszar 9,2 ha .
Położenie .
Miasto położone jest nad rzeką Iłżanką , która tworzy tu Zalew Iłżaecki , w południowej części województwa mazowieckiego , przy północnym krańcu Przedgórza Iłżeckiego – północny kraniec miasta leży już na Równinie Radomskiej .
