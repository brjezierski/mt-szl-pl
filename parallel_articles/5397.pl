Jablonec nad Nysą
Jablonec nad Nysą ( cz .
`` Jablonec nad Nisou '' , niem .
`` Gablonz an der Neiße '' ) – miasto statutarne w Czechach , nad Nysą Łużycką , u podnóża Gór Izerskich .
Drugie co do wielkości miasto kraju libereckiego , siedziba powiatu Jablonec nad Nysą .
Ośrodek produkcji sztucznej biżuterii ( `` Jablonex '' , `` G & amp ; B beads '' – szklane perły ) , szkła ( `` Preciosa '' – producent szkła kryształowego ) i elektroniki profesjonalnej ( `` Jablotron '' )
Historia .
Wieś Jablonec ( `` Gablonz '' ) została założona w XIV wieku ; pierwszy dokument pisany wzmiankujący o niej pochodzi z 1356 .
Nazwa miasta oznacza „ miejsce , gdzie rosną jabłonie ” .
W sierpniu 1496 wieś została doszczętnie spalona przez wojska Związku Miast Łużyckich podczas wojny pomiędzy nimi a królem Czech Jerzym z Podiebradów .
W XVIII wieku powstała huta szkła i fabryka sztucznej biżuterii , a jej pierwszy eksporter , J. F. Schwan , uczynił miasto znanym w Europie .
Wieś `` Gablonz '' została podniesiona do rangi gminy przez cesarza Franciszka II Habsburga 21 kwietnia 1808 , zaś prawa miejskie nadał jej cesarz Franciszek Józef I 28 marca 1866 .
W XIX wieku miasto rozwijało się .
W latach 1870–1871 wojna francusko-pruska sprawiła , że w krajach tych produkcja szkła i sztucznej biżuterii upadła , zaś kupcy z `` Gablonz '' przejęli rynki zagraniczne .
Produkcja i eksport osiągały niewiarygodnie szeroki zasięg przez następne 60 lat .
