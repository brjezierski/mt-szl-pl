Gwara jabłonkowska
Gwara jabłonkowska – gwara etnolektu śląskiego używana w południowej części historycznego regionu Śląska Cieszyńskiego , w okolicach Jabłonkowa w Czechach i na obszarze tzw .
`` Trójwsi Beskidzkiej '' w Polsce .
Jedną z najważniejszych cech tej gwary jest jabłonkowanie oraz zachowane staropolskie „ rz ” frykatywne .
