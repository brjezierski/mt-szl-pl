Jabłonna ( Otwock )
Jabłonna – dawniej samodzielna miejscowość , obecnie część ( SIMC 0921266 ) Otwocka , w województwie mazowieckim , w powiecie otwockim .
Leży we wschodniej części Otwocka , między Śródborowem a Świerkiem , ok. 1 km na południowy zachód od reaktora jądrowego EWA .
Ciągnie się wzdłuż ul .
Narutowicza .
W latach 1867–1952 wieś w gminie Wiązowna .
W 1921 roku Jabłonna liczyła 202 stałych mieszkańców .
20 października 1933 utworzono gromadę Jabłonna w granicach gminy Wiązowna , składającą się z samej wsi Jabłonna .
Podczas II wojny światowej w Generalnym Gubernatorstwie , w dystrykcie warszawskim .
W 1943 gromada Jabłonna liczyła 517 mieszkańców .
1 lipca 1952 , w związku z likwidacją powiatu warszawskiego , gminę Wiązowna ( z Jabłonną ) włączono do nowo utworzonego powiatu miejsko-uzdrowiskowego Otwock , gdzie gmina ta została przekształcona w jedną z jego ośmiu jednostek składowych – dzielnicę Wiązowna .
Dzielnica Wiązowna przetrwała do końca 1957 roku , czyli do chwili zniesienia powiatu miejsko-uzdrowiskowego Otwock i przekształcenia go w powiat otwocki .
1 stycznia 1958 ze zniesionej dzielnicy Wiązowna ( którą równocześnie przekształcono w gromadę Wiązowna ) wyłączono Jabłonnę i włączono ją do miasta Otwocka , przez co Jabłonna stała się integralną częścią miasta .
