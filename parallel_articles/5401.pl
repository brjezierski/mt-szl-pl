Jabłoń-Rykacze
Jabłoń-Rykacze – wieś w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Apostołów Piotra i Pawła w Jabłoni Kościelnej .
Historia wsi .
Wieś drobnoszlachecka założona w XV lub XVI w. Jedna ze wsi okolicy szlacheckiej `` Jabłoń '' , wzmiankowanej już w XV w. Wsie rozróżnione drugim członem nazwy .
Wymieniona w dokumentach w roku 1569 .
W I Rzeczypospolitej należała do ziemi bielskiej .
W roku 1827 wieś liczyła 16 domów i 103 mieszkańców .
Pod koniec XIX w. należała do powiatu `` mazowieckiego '' , gmina Piekuty , parafia `` Jabłoń '' .
Grunty rolne zajmowały powierzchnię 158 morgów .
We wsi znajdował się wiatrak .
W roku 1921 w miejscowości naliczono 14 budynków z przeznaczeniem mieszkalnym i 2 `` inne , zamieszkałe '' oraz 75 mieszkańców ( 37 mężczyzn i 38 kobiet ) .
Wszyscy podali narodowość polską i wyznanie rzymskokatolickie .
