Wola Jabłońska
Wola Jabłońska – wieś w Polsce położona w województwie wielkopolskim , w powiecie grodziskim , w gminie Rakoniewice .
Wieś leży przy szosie z Nowego Tomyśla do Rakoniewic .
Od północno-zachodniej strony przepływa rzeka Dojca .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
