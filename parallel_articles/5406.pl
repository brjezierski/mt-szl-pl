Jacopo Peri
Jacopo Peri ( ur .
20 sierpnia 1561 w Rzymie , zm .
12 sierpnia 1633 we Florencji ) – włoski śpiewak i kompozytor , należący do Cameraty florenckiej .
Uczeń Cristofano Malvezziego , od 1591 – pierwszy kierownik muzyczny na dworze Medyceuszy we Florencji .
Jego opera – `` Dafne '' z 1598 roku otwiera w muzyce barok .
Była ona pierwszą operą w historii światowej muzyki , jednak pierwszą zachowaną operą kompozytora jest `` Eurydyka '' ( `` Euridice '' ) z roku 1600 .
Inne dzieła Periego to : opera `` Arianna '' , balety , intermedia i madrygały .
