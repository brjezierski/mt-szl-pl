Jacques Chirac
( wym .
[ ] ; ur .
29 listopada 1932 w Paryżu , zm .
26 września 2019 tamże ) – francuski polityk .
W latach 1995–2007 prezydent Francji i jednocześnie współksiążę Andory , w latach 1974–1976 oraz 1986–1988 premier Francji , w latach 1977–1995 mer Paryża .
Był również sekretarzem stanu , ministrem , deputowanym krajowym i posłem do Parlamentu Europejskiego I kadencji , a także liderem ugrupowań gaullistowskich .
Działał na scenie politycznej przez 45 lat ; był ostatnim prezydentem Francji urodzonym przed II wojną światową i ostatnim zaangażowanym w politykę jeszcze w czasie prezydentury generała Charles ’ a de Gaulle ’ a .
Życiorys .
Działalność do 1962 .
Urodził się w Paryżu jako syn Abela Chiraca ( pracownika sektora bankowego i przedsiębiorstwa lotniczego Henry ’ ego Poteza ) oraz Marie-Louise Valette ( gospodyni domowej ) .
Był jedynym dzieckiem swoich rodziców , jego starsza siostra Jacqueline zmarła w wieku kilkunastu miesięcy jeszcze przed jego urodzeniem .
