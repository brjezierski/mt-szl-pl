Jacques Piccard
Jacques Piccard ( ur .
28 lipca 1922 w Brukseli , zm .
1 listopada 2008 w La Tour-de-Peilz ) – szwajcarski inżynier i oceanograf , zbadał dno Rowu Mariańskiego w 1960 .
Syn Auguste ' a , ojciec Bertranda .
Zarys biografii .
Studiował ekonomię , historię i fizykę w Institut de hautes études internationales et du développement w Genewie i na Université de Bâle w Bazylei .
W 1937 jego ojciec zbudował swój pierwszy batyskaf , czyli statek głębinowy .
W 1960 w ulepszonym batyskafie , „ Trieste ” , Jacques wraz z porucznikiem United States Navy , Donem Walshem zanurzył się na dno Rowu Mariańskiego .
Za swój wyczyn obaj zostali uhonorowani Legią Zasługi przez prezydenta Stanów Zjednoczonych Dwighta Eisenhowera .
