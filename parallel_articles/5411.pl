Jadon Sancho
Jadon Malik Sancho ( ur .
25 marca 2000 w Londynie ) – angielski piłkarz trynidadzkiego pochodzenia , występujący na pozycji pomocnika w angielskim klubie Manchester United oraz w reprezentacji Anglii .
Srebrny medalista Mistrzostw Europy 2021 .
Kariera klubowa .
Wychowanek Watfordu .
Następnie piłkarz akademii Manchesteru City , z której dołączył do Borussii Dortmund .
1 lipca 2021 Borussia Dortmund ogłosiła , że doszła do porozumienia z Manchesterem United w sprawie przenosin Sancho do angielskiego klubu .
23 lipca 2021 podpisał pięcioletni kontrakt z opcją przedłużenia o kolejny rok z Manchesterem United .
W nowym klubie zadebiutował 14 sierpnia 2021 w wygranym 5:1 meczu przeciwko Leeds United , zmieniając w 74. minucie spotkania Daniela Jamesa .
Swoją pierwszą bramkę dla Manchesteru United strzelił 23 listopada 2021 roku w wygranym 0:2 meczu przeciwko Villarreal .
Kariera reprezentacyjna .
W 2017 wraz z reprezentacją Anglii do lat 17 sięgnął po młodzieżowe mistrzostwo świata .
W seniorskiej reprezentacji Anglii zadebiutował 12 października 2018 w zremisowanym 0:0 meczu przeciwko Chorwacji , zmieniając w 78. minucie spotkania Raheema Sterlinga .
