Jadowniki ( województwo małopolskie )
Jadowniki – wieś sołecka w Polsce położona w województwie małopolskim , w powiecie brzeskim , w gminie Brzesko .
Wieś liczy prawie 1500 gospodarstw i 5048 mieszkańców ( 13.9 % ludności gminy ) .
Gęstość zaludnienia wynosi 259,12 os./km² .
Zajmuje powierzchnię 19,35 km² , co stanowi ok. 11,5 % powierzchni gminy .
W niektórych źródłach nazwa wsi występuje w formie Jadowniki Podgórne ( np .
w Słowniku geograficznym Królestwa Polskiego i innych krajów słowiańskich z 1888 ) , która to ma je odróżniać od Jadownik Mokrych leżących na Powiślu Dąbrowskim .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Jadowniki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnowskiego .
