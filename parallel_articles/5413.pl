Jadwiga Bilczewska-Stanecka
Jadwiga Bilczewska-Stanecka ( ur .
22 lutego 1921 w Wilamowicach , zm .
23 lutego 1976 ) – polska pedagog , założycielka i kierowniczka Regionalnego Zespołu Pieśni i Tańca „ Wilamowice ” , autorka wielu pieśni , wierszy i melodii , choreograf zespołu .
Życiorys .
Urodziła się 22 lutego 1921 w Wilamowicach .
Szkołę podstawową ukończyła w Wilamowicach , natomiast liceum ogólnokształcące w Kętach .
W 1939 zdała maturę .
Dalszą edukację przerwała wojna .
Mimo że znajdowała się na liście podejrzanych studentów , udało jej się uniknąć aresztowania przez Niemców i wyjechać do Wiednia , gdzie była w kilku domach na służbie .
W 1942 , gdy władze wiedeńskie wydały rozkaz wysiedlenia Polaków z miasta , wróciła do kraju .
Najpierw pracowała jako sekretarka w gminie w Wilamowicach , następnie była ekspedientką w sklepie w Bielsku , a potem sekretarką w tartaku w Kozach .
Współpracowała z partyzantami , przenosząc rozkazy i gazety .
W 1945 podjęła pracę w szkole podstawowej w Wilamowicach , w latach 1961–1971 była jej kierownikiem .
W 1948 założyła zespół dziecięcy Regionalny Zespół Pieśni i Tańca `` Wilamowice '' , który brał udział we wszystkich uroczystościach , wystawiał liczne przedstawienia i wieczornice .
W 1955 zespół składał się już ze starszej młodzieży i miał w swoim programie kilka tańców regionalnych .
W 1957 władze gromadzkie zlikwidowały świetlicę i zespół , który dopiero po 2 latach wznowił pracę pod protektoratem TKKF , a następnie pod szyldem cepelii w Czechowicach .
Zespół nawiązał kontakty z zespołami pieśni i tańca „ Śląsk ” i „ Mazowsze ” .
Kilka razy nagrywał swoje programy w Polskim Radiu i w Telewizji Polskiej .
Jadwiga Bilczewska-Stanecka tworzyła samodzielnie programy okolicznościowych występów .
Przez 20 lat była radną powiatową .
Zrekonstruowała męski strój wilamowicki .
Zmarła 23 lutego 1976 .
Została pochowana w rodzinnym grobowcu na cmentarzu w Wilamowicach .
Jej mężem był Mieczysław Stanecki , którego poślubiła w 1948 .
Została odznaczona Krzyżem Kawalerskim Orderu Odrodzenia Polski .
