Jagodina ( obwód Smolan )
Jagodina ( bułg .
Ягодина ) – wieś w południowej Bułgarii , w obwodzie Smolan , w gminie Borino .
Jagodina znajduje się w Rodopach .
Wieś została założona w V wieku p.n.e .
Podczas panowania tureckiego miejscowość nosiła nazwę Bałaban .
Miejscowość wzmiankowana była w osmańskich pismach z 1576 roku , gdzie wspomniano o powołaniach mieszkańców do służby panującego sułtana .
W 1635 roku wybudowano we wsi meczet .
