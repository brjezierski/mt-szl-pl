Jagielnica ( stacja kolejowa )
Jagielnica ( ukr .
Ягільниця , ros .
Ягильница ) – stacja kolejowa w miejscowości Nagórzanka , w rejonie czortkowskim , w obwodzie tarnopolskim , na Ukrainie .
Leży na linii Biała Czortkowska – Zaleszczyki – Stefaneszty .
Nazwa pochodzi od pobliskiej wsi ( dawniej miasta ) Jagielnicy .
