Jaime Penedo
Jaime Manuel Penedo Cano ( ur .
26 września 1981 w mieście Panama ) – były panamski piłkarz występujący na pozycji bramkarza .
Kariera klubowa .
Penedo w swojej karierze reprezentował barwy panamskiego Árabe Unido , włoskiego Cagliari Calcio , hiszpańskiej Osasuny i gwatemalskiego Municipalu .
W 2013 przeszedł do Los Angeles Galaxy , a w 2016 do Deportivo Saprissa .
Później dołączył do Dinama Bukareszt .
W tym klubie zakończył swoją karierę w 2018 roku .
Kariera reprezentacyjna .
Penedo w dorosłej reprezentacji Panamy zadebiutował w 2003 roku w wygranym 1:0 spotkaniu z Kubą .
Brał udział w trzech edycjach Złotego Pucharu CONCACAF : 2005 , 2007 i 2009 , 2011 , 2013 i 2015 .
Zagrał również na Copa América 2016 .
Został powołany na Mistrzostwa Świata 2018 .
Od czasu debiutu regularnie pojawiał się w meczach kadry narodowej .
Po Mundialu 2018 zakończył reprezentacyjną karierę
