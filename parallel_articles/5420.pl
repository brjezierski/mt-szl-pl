Jaipur
Jaipur ( , trb .
`` Dźajpur '' , trl .
`` Jaypur '' ; ) – metropolia w północno-zachodnich Indiach , stolica Radżastanu i popularna atrakcja turystyczna zwana `` różowym miastem '' ze względu na kolor budynków , które wyłożone są różowym piaskowcem .
Historia .
Nazwa miasta pochodzi od imienia władcy radźpuckiego Jai Singha II .
W 1727 , kiedy państwo Mogołów przeżywało już kryzys , zdecydował on o przeniesieniu stolicy z pobliskiego fortu Amber do nowo wybudowanego miasta .
Stąd Jaipur , w przeciwieństwie do wielu innych indyjskich miast , charakteryzuje się regularną , podzieloną na kwartały zabudową ( obecnie otoczone murami Stare Miasto znajduje się w północno-wschodniej części Jaipuru ) .
