Jakubiki
Jakubiki – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Kosów Lacki .
Przez wieś przepływa rzeka Kosówka .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Na końcu wsi , obok mostu , znajduje się zabytkowy młyn wodny z 1946 r. Wcześniej znajdował się tu jeszcze starszy młyn .
Zbudowany został przez Romana Kluczyńskiego .
Miejscowość jest oddalona około 5 km od byłego obozu zagłady w Treblince , położona na terenie Nadbużańskiego Parku Krajobrazowego .
Wierni wyznania rzymskokatolickiego zamieszkali w miejscowości należą do parafii Kosów Lacki .
