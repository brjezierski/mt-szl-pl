Jakubowice ( Siemianowice Śląskie )
Jakubowice – nieistniejąca wieś na terenie obecnych Siemianowic Śląskich , w rejonie Pszczelnika .
Z pochodzeniem nazwy wiąże się jedna z legend o trzech braciach : Jakubie , Michale i Siemionie ( Szymonie ) , którzy zajmowali się rybołówstwem w okolicznych stawach .
Od ich imion pochodzą części Siemianowic Śląskich – odpowiednio : dawna wieś Jakubowice , Michałkowice i Siemianowice .
Według tradycji rodowej , pod koniec XV wieku Mikołaj Mieroszewski , który jako pierwszy z rodu przybył na Górny Śląsk , otrzymał od księcia Mikołaja II lub jego brata Jana II wieś Jakubowice , a w późniejszym czasie Siemianowice i Michałkowice .
