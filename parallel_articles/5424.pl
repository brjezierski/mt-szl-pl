Jakuck
Jakuck ( `` Jakutsk '' , `` Dźokuuskaj '' ) – miasto w północno-wschodniej Syberii , na lewym brzegu Leny .
Jest stolicą i największym miastem Jakucji .
Charakterystyka .
Jakuck został założony w 1632 przez Piotra Beketowa jako warownia kozacka .
Ważny ośrodek administracyjny , naukowo-kulturalny oraz przemysłowy północno-wschodniej Syberii .
Działają port lotniczy Jakuck , lotnisko Magan oraz port rzeczny na Lenie .
W Jakucku znajduje się muzeum mamutów .
Miasto jest położone na wiecznej zmarzlinie ( wszystkie budynki muszą być posadowione na palach ) , w niedużej – jak na warunki syberyjskie – odległości ( 900 km ) od bieguna zimna półkuli północnej ( Wierchojańsk , ) .
Klimat jest wybitnie kontynentalny : bardzo długa i mroźna zima ( średnia temperatura dnia w styczniu , absolutne minimum odnotowano w lutym 1891 ) , krótkie , acz ciepłe lato ( średnia temperatura dnia w lipcu , absolutne maksimum odnotowano w lipcu 2011 ) .
Ludność stanowią głównie Rosjanie oraz Jakuci .
Miasto jest siedzibą eparchii jakuckiej Rosyjskiego Kościoła Prawosławnego .
