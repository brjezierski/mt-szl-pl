Język jakucki
Język jakucki – język Jakutów , narodu zamieszkującego wschodnią Syberię .
Mówi nim ok. 450 tys .
ludzi .
Zaliczany do języków turkijskich , jednak długi wpływ języków tungusko-mandżurskich i mongolskich wywarł duży wpływ na fonetykę , słownictwo i gramatykę .
Z języka jakuckiego rozwinął się w XVII w. język dołgański .
Edward Piekarski , językoznawca-zesłaniec , opracował w latach 1907–1930 13-tomowy `` Słownik Jakuckiego Języka '' '' . ''
Charakterystyka językowa .
Jest językiem aglutynacyjnym , ale ma też elementy fleksyjne .
Charakterystyczną cechą fonetyki jest obecność samogłosek długich oraz dyftongów ( `` ыа '' „ ya ” , `` уо '' „ uo ” , `` иэ '' „ ie ” , `` үө '' „ üö ” ) , a także najbardziej wśród języków turkijskich rozwinięta harmonia wokalna , która w języku jakuckim występuje w 3 odmianach : harmonia palatalna ( np .
* a-e & gt ; a-a ; * e-a & gt ; e-e ) , harmonia labialna ( np .
* a-u & gt ; a-y ; * e-ü & gt ; e-i ; * ö-i & gt ; ö-ü ) oraz atrakcja labialna ( * o-a & gt ; o-o ; * ö-e & gt ; ö-ö ) .
Posiada 2 deklinacje : posesywną i nieposesywną .
Nieposesywna ( `` koń '' , `` konia '' itd . )
jest deklinacją podstawową , podczas gdy posesywna ( `` mój koń '' , `` mojego konia '' itd . )
