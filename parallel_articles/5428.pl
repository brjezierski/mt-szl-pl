Jamajka
Jamajka ( ang .
`` Jamaica '' ) – wyspa i państwo na Morzu Karaibskim w archipelagu Wielkich Antyli , położone na południe od Kuby i na zachód od Haiti .
Geografia .
Jamajka leży na Morzu Karaibskim na południe od Kuby .
Wyspa o powierzchni 10 991 km² jest trzecią pod względem wielkości wyspą na Karaibach ( po Kubie i Haiti ) .
Rozciąga się 235 km od cypla Morant Point na wschodzie po Negril na zachodzie , a w najszerszym miejscu ma 82 km .
Ponad połowa wyspy wznosi się ponad 300 m n.p.m .
Najwyższym szczytem jest Blue Mountain Peak ( 2256 m n.p.m .
) .
Geolodzy są zdania , że Jamajka i sąsiadujące z nią wyspy na Morzu Karaibskim powstały miliony lat temu wskutek wypiętrzenia dawnych terenów wulkanicznych .
Niższe tereny wokół wierzchołków wyłoniły się ok. 30 mln lat temu .
Dwie trzecie wyspy nadal pokrywa czapa białego wapienia , będącego pozostałością szkieletów morskich organizmów .
Proces kształtowania się tego obszaru osiągnął punkt kulminacyjny ok. 20 mln lat temu wraz z powstaniem wyspy .
Cockpit Country oraz niezliczone jaskinie zdradzają oceaniczne pochodzenie wyspy .
Zaliczają się do nich Twin Sisters na wybrzeżu Hellshire niedaleko Kingston oraz szereg pieczar w rejonie Cockpit , zwanych Windsor Cave , które ciągną się przez niemal 3 km .
Kiedy na wyspę przybyli Europejczycy , jedynymi miejscowymi owocami były : gwajawa , ananas , jabłko cukrowe i caimito .
Większość obecnie uprawianych tu roślin została sprowadzona przez ludzi .
