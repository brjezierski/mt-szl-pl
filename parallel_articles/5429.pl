Język jamajski
Język jamajski ( nazywany lokalnie `` Patois '' lub `` Patwa '' ) – język kreolski na bazie języka angielskiego , używany na Jamajce oraz przez diasporę jamajską w różnych częściach świata ( głównie w Wielkiej Brytanii i Stanach Zjednoczonych ) .
Wykazuje pewne wpływy leksykalne języka akan używanego w Ghanie oraz języków bantu .
Używany jest głównie jako język mówiony , tworzy z angielskim tzw .
kontinuum kreolskie .
Literatura .
Język jamajski pełni także funkcję języka literackiego , pierwsza książka wydana w 1912 roku nazywała się „ Pieśni Jamajki ” ( `` Songs of Jamaica '' ) i była napisana przez Claude ' a McKaya .
W języku tym pisane są często piosenki reggae .
Gramatyka .
System czasów gramatycznych znacznie różni się od znanego z angielszczyzny .
Do zaznaczania czasów służą partykuły `` en '' oraz `` a '' .
Ortografia .
Pomimo prób uregulowania sposobu zapisu za pomocą tzw .
systemu Cassidy-LePage ortografia jamajska nie jest jeszcze w pełni ustandaryzowana , tak więc słowo `` there '' bywa zapisywane jako `` de '' , `` deh '' albo `` dere '' , zaś słowo `` three '' ( „ trzy ” ) najczęściej jest zapisywane jako `` tree '' , ale niekiedy używa się również zapisu `` tri '' lub `` trii '' , aby odróżnić je od rzeczownika `` tree '' ( „ drzewo ” ) .
Często stosuje się standardowy zapis angielski , nawet gdy dany wyraz wymawia się zupełnie inaczej .
W innych przypadkach dany zapis się utrwala , nawet gdy nie jest on czysto fonetyczny np .
`` pickney '' ( dziecko ) .
W ostatnich latach , dzięki użyciu jamajskiego na piśmie ( e-maile , fora internetowe ) , zauważa się spontaniczny proces częściowej standaryzacji zapisu .
