Jamal Musiala
Jamal Musiala ( ur .
26 lutego 2003 w Stuttgarcie ) – niemiecki piłkarz pochodzenia brytyjsko-nigeryjskiego , występujący na pozycji pomocnika w niemieckim klubie Bayern Monachium oraz w reprezentacji Niemiec .
Uczestnik Mistrzostw Świata 2022 oraz Mistrzostw Europy 2020 .
Zdobywca Ligi Mistrzów UEFA z Bayernem Monachium w sezonie 2019/2020 .
Kariera klubowa .
Bayern Monachium .
W lipcu 2019 przeszedł z drużyny Chelsea U18 do juniorskich drużyn Bayernu Monachium .
3 czerwca 2020 zadebiutował w seniorskiej piłce , wchodząc z ławki rezerwowych w meczu Bayernu Monachium II przeciwko Preußen Münster ( 3:2 ) , w rozgrywkach 3 .
Fußball-Ligi .
Pierwszą bramkę zdobył 9 czerwca 2020 w meczu 3 .
Fußball-Ligi przeciwko FSV Zwickau ( 2:0 ) .
20 czerwca 2020 zadebiutował w Bundeslidze w meczu przeciwko SC Freiburg ( 3:1 ) , zostając najmłodszym graczem Bayernu , który wystąpił w meczu ligowym mając 17 lat i 115 dni .
18 września 2020 zdobył pierwszą bramkę w Bundeslidze w meczu przeciwko FC Schalke 04 ( 0:8 ) , stając się najmłodszym strzelcem Bayernu , mając 17 lat i 205 dni , pobił poprzedni rekord należący do Roque Santa Cruza , który wynosił 18 lat i 12 dni .
Kariera reprezentacyjna .
