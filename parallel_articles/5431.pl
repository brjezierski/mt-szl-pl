James Coburn
James Harrison Coburn III ( ur .
31 sierpnia 1928 w Laurel , zm .
18 listopada 2002 w Los Angeles ) – amerykański aktor filmowy i telewizyjny pochodzenia szkocko-szwedzkiego , znany m.in .
z legendarnych westernów : `` Siedmiu wspaniałych '' , `` Garść dynamitu '' czy `` Pat Garrett i Billy Kid '' .
W 1999 otrzymał Oscara za drugoplanową rolę w filmie Paula Schradera `` Prywatne piekło '' ( 1998 ) .
Życiorys .
Wczesne lata .
Urodził się w Laurel w stanie Nebraska jako syn Mylet S. ( z domu Johnson ) i Jamesa Harrisona Coburna II .
Pochodzenie jego ojca było szkocko-irlandzkie , a matka była imigrantką ze Szwecji .
Ojciec Coburna miał firmę garażową , która została zniszczona przez wielki kryzys .
Wkrótce wraz z rodzicami przeprowadził się do Compton w Kalifornii , gdzie uczęszczał do Compton Junior College .
W 1950 wstąpił do United States Army , w której służył jako kierowca ciężarówki ; od czasu do czasu był także DJ-em wojskowej stacji radiowej w Teksasie .
Stacjonował jako żołnierz w Moguncji w Niemczech , gdzie znalazł swoje powołanie jako narrator kilku filmów szkoleniowych .
Kariera .
Po powrocie do Stanów Zjednoczonych przeniósł się do Los Angeles i zapisał się na zajęcia teatralne w Los Angeles City College .
Brał udział w kilku szkolnych produkcjach i ostatecznie zadebiutował na deskach La Jolla Playhouse w przedstawieniu `` Billy Budd '' , na podstawie powieści Hermana Melville ’ a , z Vincentem Price ’ em .
Po kilku latach spędzonych w Los Angeles przeniósł się do Nowego Jorku i studiował sztukę aktorską w konserwatorium Stelli Adler .
Po powrocie do Los Angeles zdobył rolę w produkcji telewizyjnej Studio One Sidneya Lumeta .
Na dużym ekranie zadebiutował jako Whit w westernie Budda Boettichera `` Samotny jeździec '' ( `` Ride Lonesome '' , 1959 ) u boku Randolpha Scotta i Pernella Robertsa .
Pojawił się też w serialach NBC : `` Bonanza '' ( 1959 ; 1961–1962 ) , `` Klondike '' ( 1960–1961 ) i `` Acapulco '' ( 1961 ) jako detektyw Gregg Miles .
Został zatrudniony dzięki interwencji swojego przyjaciela , Roberta Vaughna jako Britt w westernie Johna Sturgesa `` Siedmiu wspaniałych '' ( `` The Magnificent Seven '' , 1960 ) .
W dramacie wojennym Dona Siegela `` Piekło jest dla bohaterów '' ( `` Hell Is for Heroes '' , 1962 ) ze Steve ’ em McQueenem wystąpił jako kapral Frank Herpshaw , mechanik , który potrafi naprawić wszystko .
W filmie wojennym `` Wielka ucieczka '' ( `` The Great Escape '' , 1963 ) zagrał Australijczyka .
Był narratorem filmu J. Lee Thompsona `` Królowie słońca '' ( `` Kings of the Sun '' , 1963 ) .
Zmarł nagle 18 listopada 2002 na zawał serca w swoim domu w dzielnicy Beverly Hills w Los Angeles .
Miał 74 lata .
