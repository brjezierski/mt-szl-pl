Urodził się w katolickiej rodzinie Johna Stanislaua Joyce ’ a i Mary Jane Joyce w Rathgar , dzielnicy Dublina .
Był najstarszym z dziesięciorga rodzeństwa , z których dwoje umarło na tyfus .
Rodzina Joyce ’ a , poza krótkim okresem dobrobytu , żyła w nędzy .
Rodzice posłali Jamesa do szkoły jezuickiej Clongowes Wood College .
Joyce miewał lęki już od czasów młodości .
Lęk przed psami spowodowany był atakiem jednego z tych czworonogów .
Miał również lęki przed burzami z piorunami które nabył od przesądnej ciotki , która opisała je jako znak gniewu Bożego .
Miał również lęk przed morzem .
