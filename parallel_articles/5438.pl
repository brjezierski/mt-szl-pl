Jamusukro
Jamusukro ( fr .
`` Yamoussoukro '' ) – stolica konstytucyjna Wybrzeża Kości Słoniowej ( od 1983 ) .
Liczy 310 000 mieszkańców ( 2014 ) .
Burmistrzem miasta jest Kouacou Gnrangbé .
W Jamusukro urodził się pierwszy prezydent Wybrzeża Kości Słoniowej Félix Houphouët-Boigny .
Historia .
Jamusukro powstało w 1909 roku w wyniku zmiany nazwy wioski N'Gokro ( zamieszkanej w 1901 roku przez 475 mieszkańców ) .
W ciągu następnych czterech dziesięcioleci Jamusukro było niewielkim ośrodkiem handlowym .
Po uzyskaniu niepodległości przez Wybrzeże Kości Słoniowej w 1960 roku Jamusukro było „ drugą stolicą kraju ” , gdyż miasto to było miejscem narodzin , domem oraz nieoficjalną siedzibą prezydenta Félixa Houphouëta-Boigny ’ ego .
W 1964 roku Félix Houphouët-Boigny rozpoczął rozbudowę miasta , budując m.in .
autostradę z Abidżanu do Jamusukro .
