Jan Bilot
Jan Bilot ( ur .
24 września 1898 w Osłoninie , zm .
1940 r. w Twer ) – kaszubski pisarz .
Życiorys .
Na świat przyszedł jako syn Bernarda i Julianny z domu Tessemann .
W 1912 ukończył naukę w niemieckiej szkole powszechnej , po której pomagał rodzicom w prowadzeniu gospodarstwa rolnego .
Podczas I wojny światowej służył w pruskiej marynarce wojennej na torpedowcu .
Dwaj starsi bracia Jana zginęli na wojnie .
Po wojnie Bilot kontynuował edukację .
W 1925 roku uzyskał w Pelplinie kwalifikacje do wykonywania zawodu nauczyciel .
W 1928 wyjechał do Warszawy i podjął pracę w policji .
W latach 30. został zatrudniony jako policjant na Kresach Wschodnich , najpierw w Zełene , a następnie w Kosowie .
17 września 1939 Jan Bilot został aresztowany przez Sowietów i osadzony w obozie jenieckim w Ostaszkowie .
W 1940 skazano go na śmierć przez zastrzelenie .
Pochowany został na Polskim Cmentarzu Wojennym w miejscowości Miednoje .
Był mężem Marii z domu Gurskiej , ojcem Ludmiła i Haliny zmarłej jako dziecko na zesłaniu .
Opowiadania Bilota publikowane były między innymi w periodykach : `` Przyjaciel ludu kaszubskiego '' , `` Bënë buten '' , `` Gryf '' , `` Gryf Kaszubski '' , `` Zrzesz Kaszëbskô '' .
Zdaniem jednego z kaszubologów Friedhelma Hinze prace Bilota były bardzo podobne do prac Alojzego Budzisza ze względu między innymi na ich humorystyczny charakter .
Pokłosie .
W 1989 językoznawczyni z Uniwersytetu Gdańskiego Róża Wosiak-Śliwa obroniła pracę doktorską `` Kaszubszczyzna pisarzy Jana Bilota i Alojzego Budzisza '' , napisaną pod kierunkiem Edwarda Brezy .
