Jan Falandys
Jan Falandys ( ur .
18 czerwca 1956 w Sokołowie Małopolskim ) – polski zapaśnik , reprezentant Polski , olimpijczyk .
Reprezentował barwy Stali Rzeszów .
Startował w stylu wolnym w kategorii wagowej 48 kg .
20-krotny mistrz Polski ( 10-krotny indywidualny i 10-krotny drużynowy mistrz Polski ) , zdobył też dwukrotnie brązowy medal mistrzostw świata ( San Diego , 1979 r. ; Kijów , 1983 r. ) oraz srebrny ( Bukareszt , 1979 r. ) i brązowy ( Budapeszt , 1983 r. ) medal mistrzostw Europy .
Reprezentował Polskę na igrzyskach olimpijskich w Moskwie w 1980 , gdzie zajął czwarte miejsce .
Na turnieju Przyjaźń-84 , w Sofii zdobył brązowy medal .
