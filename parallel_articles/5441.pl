Jan Furtok
Jan Furtok ( ur .
9 marca 1962 w Katowicach ) – polski piłkarz , napastnik .
Kariera klubowa .
W polskiej lidze występował w barwach GKS Katowice ( 1980-88 i 1995-97 ) .
W 1986 zdobył z tym klubem Puchar Polski .
Numer 9 z którym grał w GKS zawsze będzie należał do niego , gdyż został zastrzeżony i na zawsze przypisany Furtokowi .
Strzelił 85 goli w 209 meczach ligowych .
Grał także w niemieckich klubach Hamburger SV ( wicekról strzelców Bundesligi w 1991 ) i Eintracht Frankfurt , dorzucając do swego dorobku 60 trafień w 188 grach .
Do 2009 związany był z GKS jako prezes klubu , 5 stycznia 2010 został dyrektorem sportowym kadry narodowej .
W 2017 otrzymał nagrodę „ Super Buka ” – nagrodę specjalną w GKS Katowice .
Reprezentacja Polski .
W latach 1984-1993 w reprezentacji Polski rozegrał 36 spotkań i strzelił 10 bramek .
