Jan Machulski
Jan Henryk Machulski ( ur .
3 lipca 1928 w Łodzi , zm .
20 listopada 2008 w Warszawie ) – polski aktor filmowy , teatralny i telewizyjny , profesor sztuk teatralnych , reżyser teatralny i pedagog .
Pomysłodawca Alei Gwiazd w Łodzi .
Współzałożyciel Teatru Ochoty w Warszawie ( 1970 ) .
Pedagog PWSTTviF w Łodzi .
Wieloletni dziekan Wydziału Aktorskiego tej uczelni .
Mąż aktorki Haliny Machulskiej i ojciec reżysera filmowego Juliusza Machulskiego oraz działacza politycznego Wojciecha Machulskiego , syna spoza małżeństwa .
Życiorys .
W 1947 ukończył Państwowe Gimnazjum i Liceum Koedukacyjne Pierwszego Stopnia w Aleksandrowie Łódzkim .
Absolwent ( 1950 ) ówczesnego Państwowego Gimnazjum i Liceum im .
T. Kościuszki w Łodzi ( obecnie III LO w Łodzi ) .
W 1954 ukończył studia aktorskie w Państwowej Wyższej Szkole Aktorskiej w Łodzi , a w 1971 reżyserię w Państwowej Wyższej Szkole Teatralnej w Warszawie .
Trzy lata później zaczął wykładać w łódzkiej szkole filmowej , na wydziale aktorskim .
