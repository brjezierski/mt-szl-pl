Jan Miodek
Jan Franciszek Miodek ( ur .
7 czerwca 1946 w Tarnowskich Górach ) – polski językoznawca i gramatyk normatywny , profesor nauk humanistycznych , były dyrektor Instytutu Filologii Polskiej Uniwersytetu Wrocławskiego , członek Komitetu Językoznawstwa Polskiej Akademii Nauk .
Jako członek Rady Języka Polskiego przy Prezydium PAN od 1996 r. , czyli od początku jej istnienia , jest współodpowiedzialny m.in .
za wszystkie uchwały ortograficzne Rady .
Popularyzator wiedzy o języku , znany szczególnie jako autorytet normatywny w dziedzinie języka polskiego .
Od 1968 prowadzi cotygodniową rubrykę językową `` Rzecz o języku '' ( „ Słowo Polskie ” , obecnie „ Gazeta Wrocławska ” ) .
Jest twórcą i prezenterem programów telewizyjnych : `` Ojczyzna polszczyzna '' ( 1987–2007 , cotygodniowy ) i `` Słownik polsko @ polski '' ( od 2009 , cotygodniowy ) .
