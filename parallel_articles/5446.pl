Jan Riesenkampf
Jan Riesenkampf ( ur .
1963 w Zabrzu ) – polski poeta , pisarz i tłumacz .
Zadebiutował w 1992 tomikiem `` Koszula '' .
Następnie wydał tomiki : `` Wybrane '' ( 1999 ) , `` janriesenkampf.com '' ( 2002 ) , `` Zwykły poeta '' ( 2005 ) .
Napisał sztukę teatralną `` Żegnaj Minotaurze '' Śpiewał swoją poezję na festiwalu w Brwinowie , Klubie Stodoła , Lochu Camelot itp .
Tłumacz twórczości Bułata Okudżawy , Jacques ' a Brela , Mariny Cwietajewej , Konstandinosa Kawafisa , Andrieja Rozenbauma i Lluísa Llacha ( m.in .
`` L ’ estaca '' , `` Insubmis '' ) .
Autor nowego przekładu Międzynarodówki na język polski , wydrukowanego w `` Lampie '' .
Drukowany m.in .
w pismach `` Nowy Nurt '' , `` FA-ART '' , `` Lampa i Iskra Boża '' , `` Lampa '' .
Mieszka w Warszawie .
Wielokrotnie podejmował studia na Uniwersytecie Warszawskim .
