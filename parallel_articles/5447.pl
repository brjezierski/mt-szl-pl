Jan Wyglenda
Jan Wyglenda ps .
„ Traugutt ” ( ur .
24 listopada 1894 w Brzeźnicy , zm .
28 lutego 1973 w Rybniku ) – działacz plebiscytowy , uczestnik powstań śląskich , kawaler Orderu Virtuti Militari , oficer Wojska Polskiego .
Życiorys .
Urodził się w Brzeźnicy koło Raciborza na Górnym Śląsku .
Walczył w wojsku niemieckim podczas I wojny światowej skąd wrócił jako podporucznik .
W styczniu 1919 utworzył oddział polski w rodzinnej Brzeźnicy .
W czerwcu 1919 został zastępcą komendanta głównego i kierownikiem Wydziału Wywiadu Polskiej Organizacji Wojskowej Górnego Śląska .
W I powstaniu śląskim dowodził w bitwie pod Godowem i na odcinku Olza-Oświęcim , w II powstaniu śląskim Jan Wyglenda dowodził polską samoobroną w powiecie rybnickim .
W III powstaniu śląskim był zastępcą dowódcy i szefem sztabu Grupy „ Północ ” .
Po podziale Górnego Śląska zmuszony został do opuszczenia rodzinnego obszaru przyznanego Niemcom i zamieszkał w polskim województwie śląskim .
W Wojsku Polskim został awansowany do stopnia porucznika rezerwy piechoty ze starszeństwem z dniem 1 czerwca 1919 .
W 1923 , 1924 był oficerem rezerwowym 24 pułku piechoty w Łucku .
Na stopień kapitana został mianowany ze starszeństwem z 19 marca 1939 i 11. lokatą w korpusie oficerów rezerwy piechoty .
W 1927 został naczelnikiem gminy Nowa Wieś – obecnie Wirek , dzielnica Rudy Śląskiej – a następnie starostą powiatowym w Lublińcu i Rybniku .
Członek Związku Powstańców Śląskich .
W latach II wojny światowej walczył w kampanii 1939 oraz w Polskich Siłach Zbrojnych .
Po powrocie w 1947 do Polski był aktywnym członkiem Polskiej Partii Robotniczej i Polskiej Zjednoczonej Partii Robotniczej .
Mimo to w okresie stalinowskim został uwięziony ( 1950–1956 ) na podstawie fałszywych oskarżeń .
Po uwolnieniu pracował jako urzędnik w górnictwie .
W 1971 awansowany do stopnia majora .
Zmarł w Rybniku , gdzie go pochowano .
