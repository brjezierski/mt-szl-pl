Jan Ziobro ( skoczek narciarski )
Jan Ziobro ( ur .
24 czerwca 1991 w Rabce-Zdroju ) – polski skoczek narciarski , brązowy medalista konkursu drużynowego Mistrzostw Świata 2015 w Falun , zwycięzca zawodów Pucharu Świata w grudniu 2013 w Engelbergu , letni mistrz Polski z 2013 , członek kadry narodowej Polski od sezonu 2010/2011 , olimpijczyk z Soczi .
Zadebiutował w Pucharze Świata w sezonie 2010/2011 , pierwsze punkty zdobył 12 stycznia 2013 w Zakopanem .
W trakcie sezonu letniego 2013 zajął 3. miejsce w zawodach Letniego Grand Prix w Hakubie .
W sezonie 2013/2014 był pierwszy i trzeci podczas konkursów PŚ w Engelbergu , ponadto uplasował się w pierwszej „ piętnastce ” w obu indywidualnych konkursach olimpijskich , a w konkursie drużynowym Polska z Janem Ziobro w składzie zajęła 4. miejsce .
Najwyżej w klasyfikacji generalnej PŚ uplasował się w sezonie 2013/2014 , kiedy był 25 .
Przebieg kariery .
Pierwsze skoki Jan Ziobro oddał jako siedmiolatek .
Zainteresowanie dyscypliną zyskał w związku z „ małyszomanią ” .
Długość jego pierwszej , nieustanej , próby wynosiła 7 metrów .
Na początku kariery Ziobro reprezentował barwy klubu UKS Regle Kościelisko .
We wrześniu 2003 wystartował w Ogólnopolskich Spotkaniach UKS-ów i zajął 16. miejsce w konkursie młodzików na Maleńkiej Krokwi .
W styczniu uplasował się na 15. lokacie w Pucharze Ferii na tej samej skoczni .
W lutym zajął 23. miejsce w Mistrzostwach TZN młodzików na Małej Krokwi .
Natomiast w marcu na tym obiekcie uplasował się na 15. pozycji w zawodach o Puchar Związku Szkół Mistrzostwa Sportowego .
2004/2005 .
8 czerwca Ziobro wziął udział w konkursie młodzików na obiekcie K-35 w Zakopanem , rozegranym w ramach ligi szkolnej .
