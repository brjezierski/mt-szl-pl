Jan-Krzysztof Duda
( ur .
26 kwietnia 1998 w Krakowie ) – polski szachista , arcymistrz od 2013 roku .
Zwycięzca Pucharu Świata FIDE w 2021 roku .
Kariera szachowa .
W szachy gra od 5. roku życia .
W niedługim czasie zaczął odnosić znaczące sukcesy na arenie krajowej oraz międzynarodowej .
W 2005 roku zdobył w Kołobrzegu Puchar Polski juniorów do 8 lat .
Na liście rankingowej FIDE zadebiutował 1 października 2006 roku .
W tym samym roku zdobył w Kołobrzegu brązowy medal mistrzostw Polski juniorów do 10 lat , w latach 2007 i 2008 ( w obu przypadkach w Ustroniu ) sięgając w tej kategorii po tytuły mistrzowskie .
W 2009 roku zdobył tytuł mistrza kraju , zwyciężając w Łebie w kategorii do 12 lat , natomiast w 2012 roku w Solinie kolejny – w kategorii do 18 lat .
Łącznie zdobył kilkadziesiąt medali w rozgrywkach z cyklu mistrzostw Polski ( zarówno w szachach klasycznych , jak i szybkich oraz błyskawicznych ) w różnych kategoriach wiekowych .
