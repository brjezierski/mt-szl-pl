Jane Austen urodziła się w 1775 roku w parafii Steventon .
Jej ojcem był George Austen ( 1731-1805 ) , duchowny kościoła anglikańskiego , a matką – Cassandra Austen z domu Leigh ( 1739-1827 ) .
Dochód Austenów nie był wysoki ( wynosił niewiele ponad 600 funtów rocznie ) , dlatego ich córka nie miała zbyt wysokiego posagu , co zmniejszało jej szanse na dobre zamążpójście .
Miała sześciu braci i jedną siostrę .
Najstarszy z jej braci , James ( 1765-1819 ) studiował na Oksfordzie i w 1787 roku otrzymał święcenia kapłańskie , a po przejściu ojca na emeryturę przejął po nim obowiązki w parafii Steventon .
Drugi brat , George ( 1766-1838 ) , od urodzenia był upośledzony ( prawdopodobnie głuchoniemy ) i nie mieszkał z rodziną w Steventon .
Trzeci brat , Edward ( 1767-1852 ) , został adoptowany przez bogatych krewnych , Knightów , których nazwisko przejął , gdy na początku XIX wieku odziedziczył ich majątek , Godmersham w hrabstwie Kent .
