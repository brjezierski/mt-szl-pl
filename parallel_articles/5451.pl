Jane Austen
Jane Austen ( // ; ur .
16 grudnia 1775 w Steventon , zm .
18 lipca 1817 w Winchesterze ) – angielska pisarka .
Autorka powieści opisujących życie angielskiej klasy wyższej z początku XIX wieku .
Mimo że sama wiodła stosunkowo odosobnione życie na prowincji w hrabstwie Hampshire , nie pozbawiło jej to zmysłu obserwacji i nie zubożyło dramaturgii jej utworów .
Ich fabuła najczęściej dotyczy zamążpójścia i związanych z tym problemów społecznych ( sama Austen nigdy nie wyszła za mąż ) .
Reputację dobrej pisarki zyskała już za życia – jej powieści chwalił m.in .
