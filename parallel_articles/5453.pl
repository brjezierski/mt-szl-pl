Janina Dziarnowska
Janina Dziarnowska ( ur .
1903 w Makiejewce w Zagłębiu Donieckim , zm .
22 grudnia 1992 ) – polska pisarka i tłumaczka , reportażystka , publicystka , znawczyni literatury radzieckiej .
Życiorys .
W 1918 powróciła do Polski i podjęła studia w Instytucie Pedagogicznym Wolnej Wszechnicy Polskiej .
W 1921 rozpoczęła pracę nauczycielską , a jako mieszkanka WSM na Żoliborzu , silnie zainteresowała się działalnością społeczną , organizując pozaszkolne życie młodzieży .
W 1936 została członkiem Komunistycznej Partii Polski .
Podczas okupacji pozostawała w Warszawie i organizowała nielegalne nauczanie młodzieży oraz pomagała rodzinom osób aresztowanych przez Niemców .
Ten okres swojego życia opisała potem w `` Powieści o Annie '' .
Po wojnie uczestniczyła w zagospodarowywaniu Ziem Odzyskanych , co z kolei znalazło odzwierciedlenie w powieści `` Miasto nowych ludzi '' .
Była członkiem PPR , a od 1948 roku – PZPR .
Odznaczona m.in .
Krzyżem Kawalerskim Orderu Odrodzenia Polski ( 1955 ) i Medalem 10-lecia Polski Ludowej ( 1955 ) .
W 1974 otrzymała Order Sztandaru Pracy II klasy .
Pochowana na wojskowych Powązkach ( kwatera D6-2-12 ) .
Wybrane publikacje ( powieści ) .
