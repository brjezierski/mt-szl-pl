Janosch
Janosch , właśc .
Horst Eckert ( ur .
11 marca 1931 w Zabrzu ) – urodzony na Górnym Śląsku pisarz niemieckojęzyczny , autor literatury dziecięcej .
Janosch jest autorem ponad 300 książek dla dzieci .
Książką , która dała początek całemu cyklowi pozycji dla dzieci , była `` Ach , jak cudowna jest Panama '' ( niem .
`` Oh , wie schön ist Panama '' ) .
Bohaterami cyklu są Miś i Tygrysek .
Cykl obejmuje ponad 120 pozycji ( według innych źródeł 160 ) , które przetłumaczono na ponad 30 języków , a szereg z nich wydano też w języku polskim .
Autor jest uzdolnionym grafikiem , sam ilustruje swoje opowiadania , a stworzone przez niego wizerunki wykorzystano przy tworzeniu filmów animowanych oraz ozdabianiu ubrań dla dzieci .
Życie i twórczość .
Urodził się na Zaborzu-Porembie , dzielnicy Zabrza w 1931 .
Jego ojciec `` Johann '' był hutnikiem .
Matka `` Hildegard '' , z domu Glodny , była osobą bardzo religijną .
Ze względu na alkoholowe skłonności ojca duży udział w jego wychowaniu mają dziadkowie .
W 1944 w wieku 13 lat Janosch zaczął pracę w fabryce jako ślusarz .
1 czerwca 1945 razem z rodzicami został wysiedlony do Niemiec i zamieszkał w Oldenburgu , gdzie podjął pracę w fabryce tekstylnej oraz naukę w szkole zakładowej .
Od 1953 roku mieszkał w Monachium .
Tutaj podjął naukę malarstwa , którą po paru semestrach przerwał .
Janosch , piszący w języku niemieckim , mówi o sobie : „ Czuję się Ślązakiem , to moja narodowość , to moja religia ” .
Stwierdził też w wywiadzie dla katowickiej Gazety Wyborczej :
W 1970 wydał książkę `` Cholonek , czyli dobry Pan Bóg z gliny '' ( niem .
`` Cholonek oder Der liebe Gott aus Leh '' , polskie tłumaczenie z przedmową Wilhelma Szewczyka 1975 ) – przejmującą plebejską historię o losach mieszkańców Górnego Śląska od lat 30. do 50 .
XX wieku .
Powieść w 2004 r. została przeniesiona na deski sceniczne przez Teatr Korez z Katowic .
Pseudonim Janosch powstał ( jak mówi anegdota ) , kiedy skierował do druku swoje pierwsze opowiadanie `` Historia konia Wałka '' ( 1960 ) i podpisał je „ Janusz ” , co niemiecki zecer „ poprawił ” na Janosch .
Obecnie Janosch – Horst Eckert od lat mieszka w San Miguel na Teneryfie .
W 1946 roku tzw .
„ komisja weryfikacyjna ” przyznała rodzinie Eckertów obywatelstwo polskie .
Wyróżnienia i spuścizna .
Za swoją twórczość wielokrotnie nagradzany , w tym Federalnym Krzyżem Zasługi oraz Nagrodą Górnośląską , przyznawaną przez śląskie ziomkostwo .
Od 2005 r. katowicka redakcja Gazety Wyborczej przyznaje nagrodę Cegły Janoscha Ślązakom , którzy rozsławiają region .
Wyróżnieni w nagrodę otrzymują prawdziwe cegły wykute ze ścian familoka w Zabrzu , w którym Janosch przyszedł na świat .
Cegły wydobył jeden z dziennikarzy Wyborczej , tuż przed rozbiórką budynku .
W 2015 r. ukazała się książka autorstwa Angeli Bajorek pt .
`` Heretyk z familoka .
Biografia Janoscha '' , opisująca życie pisarza .
Kontrowersje .
Premier Bawarii Edmund Stoiber nazwał Janoscha `` fałszywym prorokiem '' po tym , jak `` Der Spiegel '' opublikował rysunek pisarza , przedstawiający księdza , który podczas chrztu wbijał dziecku krzyżyk młotkiem .
Podczas pobytu w Zabrzu na początku lat 90 .
Janosch podarował miejskiemu teatrowi swoją sztukę pt : „ Powrót do Uskow ” – o wydźwięku antyklerykalnym i atakującą papieża Jana Pawła II .
Jeden z zabrzańskich prezydentów nieświadomy treści przekazał ją do tłumaczenia jednemu z miejskich proboszczów .
Wybuchł skandal , sztuki nie wystawiono .
