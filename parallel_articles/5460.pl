Janusz Atlas
Janusz Aleksander Atlas ( ur .
30 stycznia 1949 w Warszawie , zm .
3 stycznia 2010 tamże ) – polski dziennikarz sportowy .
Życiorys .
Trzykrotny laureat Piłkarskich Oskarów Canal+ dla najlepszego dziennikarza prasowego .
Do listopada 2008 był zastępcą redaktora naczelnego tygodnika „ Piłka Nożna ” .
W czasopiśmie zajmował się przede wszystkim tematyką korupcji w polskim futbolu , a także Ekstraklasą i polską piłką reprezentacyjną .
Pisał również felietony `` Sytuacje podbramkowe '' .
Współpracował z redakcjami „ Sztandaru Młodych ” , „ Sportowca ” , „ Motoru ” , „ Szpilek ” oraz „ Tempa ” .
1 października 2009 został rzecznikiem prasowym Polskiego Związku Piłki Nożnej .
29 października tego samego roku zarząd PZPN podjął decyzję o zastąpieniu go absolwentką Wydziału Dziennikarstwa i Nauk Politycznych Uniwersytetu Warszawskiego Agnieszką Olejkowską .
Był autorem kilku książek , związanych nie tylko z tematyką sportową .
Zagrał także epizodyczne role w kilku filmach .
Postanowieniem Prezydenta RP z dnia 17 maja 2001 `` za zasługi dla rozwoju sportu i publicystyki sportowej '' został odznaczony Złotym Krzyżem Zasługi .
Zmarł na szpiczaka .
Został pochowany 8 stycznia 2010 na Cmentarzu Wojskowym na Powązkach w Warszawie ( kwatera 23B-6-12 ) .
Życie prywatne .
Syn Stefana ( 1920–1985 ) , dziennikarza radiowego .
Był żonaty z Katarzyną Dowbor i związany z Hanną Bakułą .
Miał dwóch synów : Michała i Aleksandra .
