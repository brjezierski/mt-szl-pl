Janusz Korwin-Mikke
Janusz Ryszard Korwin-Mikke , krótko Korwin ( ur .
27 października 1942 w Warszawie ) – polski polityk prawicowy , publicysta i brydżysta , z wykształcenia filozof .
Poseł na Sejm I i IX kadencji ( 1991–1993 , od 2019 ) , deputowany do Parlamentu Europejskiego VIII kadencji ( 2014–2018 ) .
W czasach PRL zajmował się aktywizmem , za który w latach 60. był dwukrotnie więziony ; potem działał m.in .
wydawniczo , do lat 80. będąc członkiem Stronnictwa Demokratycznego .
Po nieudanych próbach wpływu na ruch „ Solidarność ” zaczął go krytykować .
Od początku III RP startował w ponad 20 różnych wyborach , w tym pięciokrotnie kandydował na urząd prezydenta – we wszystkich kolejnych głosowaniach w latach 1995–2015 .
Założył cztery partie i im przewodniczył ; były to kolejno :
W 2019 roku został także jednym z liderów federacyjnej partii Konfederacja Wolność i Niepodległość .
Korwin-Mikke od dekad deklaruje konserwatywny liberalizm – popiera monarchiczne , kapitalistyczne państwo minimum , wzorowane na tych z XIX wieku i na Liechtensteinie .
Doktryna Korwin-Mikkego obejmuje co najmniej kilkadziesiąt postulatów ; bywa zaliczana do prawoskrętnego libertarianizmu i nazywana korwinizmem .
Jego sympatycy i zwolennicy , np .
wyborcy , bywają znani jako korwiniści lub – mniej formalnie – jako kuce .
Niektórzy politolodzy nazywają Korwin-Mikkego populistą i przejawia on cechy kojarzone z tym zjawiskiem , np .
wrogość do większości klasy politycznej i jej przywilejów ; zarzuca jej opresyjność , głosi emancypację od niej i przedstawia swoje postulaty jako szansę na cud gospodarczy .
Z drugiej strony głosi także opinie bardzo niepopularne i jest wyraźnym elitarystą – wypowiadał się lekceważąco o większości ludu , odrzucając wartość jego woli czy interesu .
Swoje opinie propagował przez książki , czasopisma – m.in .
założony przez siebie tygodnik „ Najwyższy Czas ! ” – oraz wystąpienia publiczne , blogi , vlogi i media społecznościowe .
Wywoływał tym skandale , np .
przez używanie wulgaryzmów , ataków osobistych , odczłowieczanie przeciwników ideowych , oskarżanie ich o faszyzm , porównania do nazistów i wypowiedzi uznawane za dyskryminujące lub uprzedzone , np .
rasistowskie , antysemickie , islamofobiczne , seksistowskie , homofobiczne , transfobiczne i ableistyczne , choć wspierał fundację służącą ludziom niepełnosprawnym .
Używając porównań do nazizmu , parafrazował przy tym hasła III Rzeszy i wykonywał jej charakterystyczne gesty jak salut rzymski , za co był dyscyplinowany przez Parlament Europejski .
