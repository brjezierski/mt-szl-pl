Odróżnia go to od ściśle rozumianych libertarian , opierających się na aksjomacie nieagresji .
Bronił też molestowania seksualnego i się do niego przyznawał .
W brydżu Korwin-Mikke osiągnął status mistrza krajowego , należał do kadry narodowej i opublikował kilka książek o tej grze .
Angażował się też w organizacje szachowe .
