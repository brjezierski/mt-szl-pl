Z drugiej strony bronił niepodległości Ukrainy i Białorusi , uzasadniając tym poparcie dla dyktatury Aleksandra Łukaszenki .
Bronił także innego autorytaryzmu sprzymierzonego z Rosją : syryjskiego reżimu Baszszara al-Asada .
