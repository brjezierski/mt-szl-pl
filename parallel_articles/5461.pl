Polityka zagraniczna .
Popierał sojusz Polski z USA , jednak krytykował członkostwo w NATO , stacjonowanie w Polsce wojsk USA i sprzeciwiał się konfrontacji z Rosją .
Poparł aneksję Krymu , po czym odwiedził ten półwysep , nie kryjąc się z tym .
Zrobił to bez kontroli granicznej ze strony Ukrainy , łamiąc jej przepisy .
Reagowała na to ukraińska prokuratura , a w odpowiedzi Korwin-Mikke apelował do niej o realizm .
Występował w rosyjskiej telewizji , gdzie zarzucał polskim władzom rusofobię .
Po rozpoczęciu przez Rosję inwazji z 2022 roku krytykował sankcje , sugerował , że Rosja się broni i twierdził , że po wojnie Ukraina może zaatakować Polskę .
Wybielał rosyjską historię i jej działania , demonizując przy tym Ukrainę .
