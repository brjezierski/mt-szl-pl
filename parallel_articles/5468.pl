Madżong japoński
Madżong japoński ( jap .
麻雀 , 麻将 , albo マージャン ; mājan , znany również jako Riichi Madżong ) – wersja madżonga , w której podstawowe reguły gry są zachowane , jednak ponadto wprowadzone takie jak `` riichi '' oraz `` dora '' .
W tej wersji gry nie używa się już terminów „ chow ” , „ pong ” , „ kong ” , tylko „ chi ” , „ pon ” , „ kan ” .
Historia .
W 1924 żołnierz Saburo Hirayama przywiózł tę grę do Japonii .
Następnie w Tokio założył klub , salon i szkołę grania w madżonga .
Na przestrzeni lat gra ta zaczynała zdobywać coraz większą popularność .
W tym procesie , gra została uproszczona w chińskiej wersji .
Dodatkowe reguły zostały dodane , żeby zwiększyć kompleksowość .
Riichi Madżong w 2010 był najpopularniejszą grą planszową w Japonii .
