Język japoński
– język używany przez ok. 130 mln mieszkańców Japonii oraz japońskich emigrantów na wszystkich kontynentach .
W Japonii nie ma prawa , które definiowałoby japoński jako język urzędowy Japonii .
Historia i pochodzenie .
Pochodzenie języka japońskiego jest nieznane i jest przedmiotem sporów wśród językoznawców .
Wysuwane są hipotezy , jakoby należał on do języków uralo-ałtajskich – jak turecki i mongolski – lub jakoby był spokrewniony z polinezyjskimi .
