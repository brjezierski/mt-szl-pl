Żadna z hipotez nie zdobyła rozstrzygającego uznania i japoński pozostaje najczęściej używanym językiem , którego pochodzenie nie zostało jednoznacznie ustalone na gruncie lingwistyki .
Adaptacja chińskich znaków od VI wieku była najważniejszym wydarzeniem w rozwoju języka .
Do XII wieku sylabiczne systemy zapisu fonetycznego : hiragana i katakana , zostały ukształtowane z kanji , pozwalając Japończykom na pisanie w swoim ojczystym języku .
Z biegiem stuleci napływały do japońskiego tysiące słów z języków obcych : portugalskiego , holenderskiego , angielskiego , chińskiego .
Wpływ tych dwóch ostatnich języków jest zdecydowanie największy .
Najstarsze znane teksty to pochodzące z V i VI wieku nazwy własne .
Nie mówią jednak one wiele o języku .
