Jared Padalecki
Jared Tristan Padalecki ( ur .
19 lipca 1982 w San Antonio ) – amerykański aktor filmowy i telewizyjny , najlepiej znany z roli Sama Winchestera w serialu fantasy `` Nie z tego świata '' ( `` Supernatural '' ) , choć początkowo zdobył uznanie jako Dean Forester z serialu `` Kochane kłopoty '' .
Życiorys .
Wczesne lata .
Urodził się w San Antonio w Teksasie w rodzinie agnostyków jako jedno z trojga dzieci Sharon L. Kammer „ Sherri ” Padaleckiej , nauczycielki języka angielskiego w szkole średniej , i Geralda R. „ Jerry ’ ego ” Padaleckiego , księgowego i nadzorcy podatkowego .
Jego ojciec miał polskie pochodzenie , a matka była pochodzenia angielskiego , niemieckiego , szkockiego i francuskiego .
Wychowywał się ze starszym bratem Jeffem i młodszą siostrą Megan .
Jego dziadek ze strony ojca , Willie Alexander Padalecki ( syn Alberta Padaleckiego - ur .
w Dziekanowicach - i Anny Rakowitz ) , był Polakiem .
W wieku dwunastu lat zaczynał brać prywatne lekcje aktorstwa .
W 1999 zwyciężył w programie Fox Broadcasting Company „ Pretendować do Turnieju Sławy ” , zdobywając nagrodę Teen Choice .
W 2000 ukończył James Madison High School w San Antonio i w 2000 został wybrany na kandydata Szkolnego Programu Reprezentacyjnego .
Kariera .
Przeprowadził się do Los Angeles i zapoczątkował karierę aktorską , planując studia na University of Texas w Austin .
