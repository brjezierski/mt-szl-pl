Jarosław Kaczyński
Jarosław Aleksander Kaczyński ( ur .
18 czerwca 1949 w Warszawie ) – polski polityk i doktor nauk prawnych .
Brat bliźniak Lecha Kaczyńskiego .
Prezes partii Prawo i Sprawiedliwość ( od 2003 ) , w latach 2006–2007 prezes Rady Ministrów , w latach 2020–2022 wiceprezes Rady Ministrów .
W okresie PRL działacz opozycji politycznej .
Senator I kadencji ( 1989–1991 ) , poseł na Sejm I , III , IV , V , VI , VII , VIII i IX kadencji ( 1991–1993 , od 1997 ) .
Od czasu wygranych przez Prawo i Sprawiedliwość wyborów parlamentarnych w 2015 roku , będąc posłem na Sejm RP , a następnie wicepremierem , jest uważany za jedną z najbardziej wpływowych osób w Polsce .
Kandydat na urząd prezydenta RP w przedterminowych wyborach prezydenckich w 2010 .
Życiorys .
Dzieciństwo .
Urodził się w Warszawie na Żoliborzu .
Jego ojciec , Rajmund Kaczyński , z zawodu inżynier , był żołnierzem Armii Krajowej i uczestnikiem powstania warszawskiego .
Matka , Jadwiga z domu Jasiewicz , należała do Szarych Szeregów , z wykształcenia była filologiem polskim ( zawodowo związana z Instytutem Badań Literackich Polskiej Akademii Nauk ) .
Jego bratem bliźniakiem był Lech Kaczyński , późniejszy prezydent RP .
W 1962 bracia zagrali role Jacka ( Lech ) i Placka ( Jarosław ) w filmie `` O dwóch takich , co ukradli księżyc '' w reżyserii Jana Batorego , ekranizacji powieści pod tym samym tytułem Kornela Makuszyńskiego .
Wykształcenie i działalność zawodowa .
Uczęszczał początkowo do XLI Liceum Ogólnokształcącego im .
Joachima Lelewela w Warszawie .
Nie zdał tam do następnej klasy , jednak „ po odkręceniu tego ” zmienił szkołę i ostatecznie ukończył w 1967 XXXIII Liceum Ogólnokształcące im .
Mikołaja Kopernika w tymże mieście .
Studiował na Uniwersytecie Warszawskim .
W 1971 na Wydziale Prawa i Administracji UW uzyskał tytuł magistra .
Na tym samym roku studiowali z nim m.in .
jego brat Lech , Andrzej Rzepliński , Marek Safjan , Bogusław Wołoszański i Mirosław Wyrzykowski .
8 grudnia 1976 na WPiA UW obronił pracę doktorską pt .
`` Rola ciał kolegialnych w kierowaniu szkołą wyższą '' .
