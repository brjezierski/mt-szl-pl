Jaroty ( gmina Stawiguda )
Jaroty – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie olsztyńskim , w gminie Stawiguda .
Jest to kolonia dawnej wsi Jaroty , której tereny przyłączono do Olsztyna .
Obecnie stanowi samodzielne sołectwo .
Wieś usytuowana jest przy granicy administracyjnej Olsztyna .
W pierwszej dekadzie XXI w. nastąpił dynamiczny rozwój wsi , która powoli traci swój rolniczy charakter .
Od 2000 r. jest tu obserwowany znaczny wzrost liczby domów wielorodzinnych , w zabudowie szeregowej oraz domków jednorodzinnych .
W 2008 r. nadano pierwsze nazwy ulicom znajdującym się w osadzie .
Od 2000 r. do 2016 r. liczba ludności osady wzrosła o 4788 % ( z 27 osób do 1320 osób ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
