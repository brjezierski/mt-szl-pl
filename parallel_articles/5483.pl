Jaryłówka ( Białoruś )
Jaryłówka ( ; ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie wołkowyskim , w sielsowiecie Gniezno , przy drodze republikańskiej .
Dawniej wieś i majątek ziemski .
W dwudziestoleciu międzywojennym leżała w Polsce , w województwie białostockim , w powiecie wołkowyskim , w gminie Mścibów .
