Jastarnia
Jastarnia ( kaszb .
`` Jastarniô '' , niem .
`` Putziger Heisternest '' ) – miasto w województwie pomorskim , w powiecie puckim , w gminie Jastarnia , na Mierzei Helskiej .
Kurort nadmorski z pięcioma letnimi kąpieliskami morskimi , portem morskim i trzema przystaniami morskimi .
Według danych z 31 grudnia 2016 r. obecny obszar miasta ( wtedy w gminie miejskiej Jastarnia ) miał 2734 mieszkańców .
1 stycznia 2017 Jastarnia zmieniła charakter z gminy miejskiej na miejsko-wiejską przez wydzielenie osobnych miejscowości Juraty i Kuźnicy z Syberią .
Położenie .
Według danych z 1 stycznia 2017 r. powierzchnia miasta wynosiła 4,31 km² , co stanowi 55,3 % powierzchni gminy Jastarnia .
Wieś królewska w starostwie puckim w powiecie puckim województwa pomorskiego w II połowie XVI wieku .
W latach 1975–1998 miasto administracyjnie należało do województwa gdańskiego .
