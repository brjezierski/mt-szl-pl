Toponimia .
Nazwa Jastarnia może pochodzić od słowa `` Jastra '' , oznaczającego coś jasnego lub `` Jastra '' oznaczającego w języku kaszubskim Wielkanoc .
Dawniej funkcjonowały nazwy kaszubskie `` Pùckô Jastarniô '' , `` Sroczé Gniôzdo '' oraz niemiecka `` Heisternest '' .
Polskimi nazwami miejscowości były `` Jasturnia Pucka '' , `` Zesterna '' , `` Niastarnia '' .
