Jaszczurki
Jaszczurki ( Lacertilia , dawniej Sauria ) – grupa gadów łuskonośnych obejmująca czworonożne lub beznogie zwierzęta lądowe o wydłużonym ciele , oczach posiadających powieki , mocnych szczękach i diapsydalnej czaszce .
Występują na wszystkich kontynentach poza Antarktydą oraz na wielu wyspach oceanicznych .
Są najliczniejszą grupą gadów obejmującą ponad 4000 gatunków .
Taksonomia .
W tradycyjnym rozumieniu grupa ta obejmuje wszystkie łuskonośne ( Squamata ) nienależące do węży ani do amfisben .
Tak definiowane jaszczurki są taksonem parafiletycznym , ponieważ nie obejmują tych dwóch ostatnich grup , lecz obejmują ich przodków .
Będąc grupą parafiletyczną , nie są wyróżniane w niektórych nowszych klasyfikacjach , m.in .
w klasyfikacji ITIS .
