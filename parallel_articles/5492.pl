Jasło ( stacja kolejowa )
Jasło – stacja kolejowa w Jaśle , w województwie podkarpackim , w Polsce .
Główny węzeł kolejowy na południu województwa podkarpackiego .
Ze stacji wychodzą linie w stronę Stróż , Rzeszowa , Zagórza i dalej na Słowację i Ukrainę .
Stacja i perony są w pełni zelektryfikowane w kierunku Stróż i Jasło Towarowa .
W roku 2017 stacja obsługiwała 100–149 pasażerów na dobę .
