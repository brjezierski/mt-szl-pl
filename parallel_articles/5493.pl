Jataí
Jataí – miasto i gmina w Brazylii leżące w stanie Goiás .
Gmina zajmuje powierzchnię 7174,22 km² .
Według danych szacunkowych w 2016 roku miasto liczyło 97 077 mieszkańców .
Położone jest około 320 km na południowy zachód od stolicy stanu , miasta Goiânia , oraz około 560 km na południowy zachód od Brasílii , stolicy kraju .
W 1864 roku na tym obszarze istniała miejscowość Paraíso .
31 maja 1895 roku ówczesna wieś otrzymuje prawa miejskie i przybiera obecną nazwę .
W 2014 roku wśród mieszkańców gminy PKB per capita wynosił 37 712,84 reais brazylijskich .
