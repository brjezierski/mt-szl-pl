Jaunde
Jaunde ( ang .
`` Yaounde '' , fr .
`` Yaoundé '' ) – stolica i drugie co do wielkości miasto Kamerunu – ludność : 1,9 mln ( 2019 ) .
Stolica departamentu Mfoundi .
Położone na wyżynie w południowo-zachodniej części Kamerunu , na wysokości 730 m n.p.m .
Jaunde zostało założone w 1888 przez niemieckich handlarzy kością słoniową .
W czasie I wojny światowej miasto było okupowane przez wojska belgijskie .
W 1922 stało się stolicą terytorium mandatowego Ligi Narodów pod administracją francuską – Kamerunu Francuskiego .
Po uzyskaniu niepodległości w 1960 Jaunde zostało stolicą kraju .
Ośrodek handlu i przemysłu tytoniowego , spożywczego , ceramicznego i drzewnego .
Węzeł transportowy – lotnisko międzynarodowe i krajowe , linie kolejowe do Ngaoundéré i Duala , prywatne linie autobusowe .
Przez miasto przebiegają drogi krajowe nr 1 , 2 , 3 i 10 .
Jaunde jest siedzibą instytucji rządowych i przedstawicielstw zagranicznych ( w dzielnicy Bastos ) .
Główne atrakcje to Kameruńskie Muzeum Sztuki ( w klasztorze benedyktyńskim ) , Kameruńskie Muzeum Narodowe ( w dawnym pałacu prezydenckim ) , Muzeum Afhemi , ogród zoologiczny ( w dzielnicy Mvog-Betsi ) .
W mieście znajdują się Uniwersytet w Jaunde i Catholic University for Central Africa .
Jest w nim stadion im .
