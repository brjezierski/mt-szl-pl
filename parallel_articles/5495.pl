Javier Saviola
Javier Pedro Saviola Fernández ( ur .
11 grudnia 1981 w Buenos Aires ) – argentyński piłkarz , reprezentant Argentyny występujący na pozycji napastnika lub pomocnika .
Posiada także obywatelstwo hiszpańskie .
W latach 2001–2007 zawodnik FC Barcelony .
Początki kariery .
Karierę piłkarską rozpoczynał w klubie Club Atlético River Plate .
Dzięki dobrej grze w październiku 1998 roku został powołany do pierwszego składu w meczu przeciwko Gimnasia y Esgrima de Jujuy .
Od tego czasu stał się ulubieńcem kibiców oraz został powołany do młodzieżowej reprezentacji drużyny Argentyny .
W 1999 roku zdobył z klubem El Trofeo Apretura oraz wystąpił wraz z reprezentacją na Mistrzostwach Świata U-20 , gdzie oprócz zwycięstwa drużyny , zdobył sobie także tytuł króla strzelców oraz najlepszego zawodnika turnieju .
Osiemnastoletni Saviola został wyróżniony przez urugwajski dziennik El Pais jako najlepszy piłkarz Ameryki Południowej sezonu 1999/2000 .
Kariera klubowa .
Młodym napastnikiem interesowały się europejskie kluby .
Walkę o piłkarza wygrała FC Barcelona , płacąc prawie 25 milionów € .
Dobrą grę z Ameryki Południowej przeniósł do Europy i w pierwszym swoim sezonie na Starym Kontynencie strzelił 21 goli , dzięki czemu został uznany najlepszym graczem z Ameryki Południowej w Primera Division 2001/02 .
Jego gol w drugim ćwierćfinałowym meczu z Panathinaikosem Ateny w Lidze Mistrzów 2001/02 dało Barcelonie awans do półfinału .
Na początku sezonu 2002/03 , gdy do klubu powrócił Louis van Gaal , Saviola stracił pozycję w drużynie , w kilku spotkaniach strzelił dwie bramki .
Kiedy w drugiej części sezonu na ławce trenerskiej zasiadł Radomir Antić , Javier odzyskał miejsce w ekipie .
Wystąpił w siedemnastu ligowych spotkaniach , w których strzelił 11 goli , a kolejne 7 bramek zdobył w Lidze Mistrzów .
W sezonie 2003/04 posadę trenera objął Frank Rijkaard .
Argentyńczyk strzelił w lidze 14 goli , dzięki którym Barcelona awansowała do Ligi Mistrzów .
Wyjazd na letnie IO i niechęć wobec obniżenia zarobków z kontraktu podpisanego w czasie prezesury Joana Gasparta były jednymi z czynników , którymi kierował się Rijkaard , rezygnując z usług Argentyńczyka .
W sezonie 2004/05 został wypożyczony do AS Monaco .
W tym sezonie strzelił 8 bramek w Ligue 1 , 5 w Pucharze Francji i 4 w Lidze Mistrzów .
W sezonie 2005/06 grał na zasadzie wypożyczenia w Sevilli , z którą zdobył Puchar UEFA .
Javier wrócił do Barçy na czas sezonu 2006/07 .
W roku 2007 wygasł jego kontrakt z `` Dumą Katalonii '' , po czym zdecydował się na transfer do odwiecznego rywala Barcelony - Realu Madryt .
Przez dwa sezony ( 2007/08 oraz 2008/09 ) w barwach `` Królewskich '' rozegrał 31. spotkań , w których zdobył 5. bramek .
Po zakończeniu sezonu 2008/09 za kwotę 5 milionów € przeszedł do `` As Águias '' .
W styczniu 2012 roku przedłużył swój kontrakt z Benficą do 30 czerwca 2013 .
Javier Saviola podpisał kontrakt z Málagą CF – poinformowała oficjalna witryna klubu z Andaluzji .
Umowa obowiązywała przez jeden sezon .
Saviola rozwiązał kontrakt ze swoim wcześniejszym klubem , którym była SL Benfica .
Argentyńczyk podpisał kontrakt ze swoim nowym klubem na Estadio La Rosaleda , gdzie wcześniej obejrzał losowanie grup Ligi Mistrzów , w której wystąpił w barwach Málagi .
W lipcu 2013 roku Málaga nie przedłużyła kontraktu z graczem .
W lipcu 2013 roku przeszedł na zasadzie wolnego transferu do greckiego klubu Olympiakos SFP .
Latem 2014 został zawodnikiem Hellas Werona .
Reprezentacja .
W 2004 Javier pojechał wraz z reprezentacją Argentyny do Peru , gdzie odbywał się turniej Copa América 2004 , na których Argentyna zajęła 2. miejsce .
Saviola strzelił 3 gole , wszystkie w meczu fazy grupowej z Ekwadorem .
W tym samym roku wystąpił też na igrzyskach olimpijskich w Atenach .
Strzelił jedną bramkę i wywalczył wraz z kolegami z drużyny złoty medal olimpijski po finale z Paragwajem .
W 2005 roku wraz z reprezentacją Argentyny pojechał do Niemiec , gdzie odbywały się rozgrywki o Puchar Konfederacji 2005 .
Javier nie zagrał jednak w finale , ponieważ otrzymał czerwoną kartkę w meczu półfinałowym z Meksykiem .
Ostatecznie Argentyna przegrała w finale z Brazylią 4:1 .
W czerwcu 2006 roku zagrał na Mistrzostwach Świata w Niemczech .
Swoją jedyną bramkę w turnieju strzelił w meczu fazy grupowej z Wybrzeżem Kości Słoniowej .
Argentyna odpadła w 1/4 finału w meczu z Niemcami ( 1:1 , 4:2 w rzutach karnych ) .
5 grudnia 2009 zdecydował o zakończeniu reprezentacyjnej kariery .
Swoją decyzję umotywował chęcią skupienia się na karierze klubowej .
