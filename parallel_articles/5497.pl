Jawhar
Jawhar ( także : `` Jowhar '' , arab .
`` Dżauhar '' ) – miasto w Somalii ; 48 900 mieszkańców ( 2006 ) .
Przemysł spożywczy , włókienniczy .
Miasto jest położone 50 km na północ od stolicy , Mogadiszu , w dolinie rzeki Uebi Szebelie .
Historia .
Założone w 1920 przez Luigi Amadeo di Savoia jako kolonia rolnicza , nadając jej nazwę `` Villaggio Duca degli Abruzzi '' .
Znaczny rozwój miasta miał miejsce w drugiej połowie lat trzydziestych , kiedy osiedliło się tu tysiące kolonizatorów włoskich .
Po zakończeniu drugiej wojny światowej , po odejściu włoskich kolonizatorów , miasto zaczęło tracić na znaczeniu , zwłaszcza po wybuchu wojny domowej w Somalii w 1991 .
W 27 grudnia 2006 została zajęta przez wojska etiopskie , które wsparły Tymczasowy Rząd Somalijski .
Jednak w 2008 milicje islamskie zdołały odzyskać miasto i odciąć od rządu somalijskiego wspieranego przez Narody Zjednoczone .
