Powiat jaworski
Powiat jaworski – powiat w Polsce ( województwo dolnośląskie ) , utworzony w 1999 roku w ramach reformy administracyjnej .
Siedzibą jego władz jest miasto Jawor .
W skład powiatu wchodzą :
Demografia .
Według danych z 31 grudnia 2019 roku powiat zamieszkiwało 50 116 osób .
Natomiast według danych z 30 czerwca 2020 powiat zamieszkiwało 49 937 osób .
Gospodarka .
W końcu listopada 2019 liczba zarejestrowanych bezrobotnych w powiecie obejmowała ok. 1,7 tys .
mieszkańców , co stanowi stopę bezrobocia na poziomie 10,6 % do aktywnych zawodowo .
