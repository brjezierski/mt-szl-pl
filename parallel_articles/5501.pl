Jaworze ( województwo zachodniopomorskie )
Jaworze ( niem .
`` Gabbert '' ) – wieś w Polsce położona w województwie zachodniopomorskim , w powiecie drawskim , w gminie Kalisz Pomorski .
W latach 1975–1998 miejscowość administracyjnie należała do województwa koszalińskiego .
W roku 2007 wieś liczyła 5 mieszkańców .
Miejscowość wchodzi w skład sołectwa Prostynia .
