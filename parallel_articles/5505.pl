Jay Sebring
Jay Sebring ( właściwie Thomas John Kummer ; ur .
10 października 1933 w Birmingham , zm .
9 sierpnia 1969 w Beverly Hills ) – amerykański stylista i fryzjer mieszkający w Hollywood , okazjonalnie aktor .
Życiorys .
Jego rodzicami byli Bernard i Margarette Kummer .
Wychował się na przedmieściach Detroit , miał czworo rodzeństwa .
W czasie wojny koreańskiej służył w amerykańskiej marynarce wojennej , gdzie nauczył się fryzjerstwa .
Po powrocie przybrał nowe nazwisko i otworzył pierwszy nowoczesny salon fryzjerski dla mężczyzn w Stanach Zjednoczonych , dzięki czemu stał się celebrytą .
Był właścicielem dwóch salonów fryzjerskich : jednego w San Francisco , drugiego w Los Angeles .
Był fryzjerem i stylistą gwiazd , m.in .
Warrena Beatty ’ ego , Paula Newmana , Jima Morrisona , Franka Sinatry , czy Steve ’ a McQueena .
Wystąpił jako aktor w kilku serialach , m.in .
w jednym z odcinków serialu `` Batman '' jako Mr. Oceanbring .
Pobierał lekcje sztuk walki od Bruce ’ a Lee , któremu pomógł też w uzyskaniu pierwszej poważnej roli w programie telewizyjnym `` The Green Hornet , '' przekazując producentowi `` Batmana '' – Williamowi Dozierowi – taśmę z nagraniami .
Jako fryzjer i stylista brał udział w produkcji wielu znanych filmów .
Był założycielem przedsiębiorstwa `` Sebring International '' , które specjalizuje się w produktach kosmetycznych dla mężczyzn .
Został zamordowany przez członków sekty `` Rodzina '' ( ang .
`` The Family '' ) , której przywódcą był Charles Manson , w rezydencji wynajmowanej przez reżysera Romana Polańskiego i jego żonę , aktorkę filmową Sharon Tate .
Wśród ofiar mordu byli również jego przyjaciele – Tate , Abigail Folger i Wojciech Frykowski , a także Steven Parent .
Sprawcy zbrodni zostali skazani na kary dożywotniego pozbawienia wolności w kalifornijskich więzieniach .
Jay Sebring został pochowany na cmentarzu `` Holy Sepulchre Cemetery '' w mieście Southfield w stanie Michigan dnia 13 sierpnia 1969 .
Jego postać znalazła się w filmie `` Pewnego razu ... w Hollywood '' w reżyserii Quentina Tarantino z 2019 roku , gdzie odtwarzał ją Emile Hirsch .
Na historii Sebringa oparto także głównego bohatera filmu `` Szampon '' w reżyserii Hala Ashby ’ ego z 1975 roku .
Jego przybrane nazwisko , `` Sebring '' , pochodzi od nazwy toru wyścigowego na Florydzie .
