Jaén ( prowincja )
Jaén – prowincja położona na południu Hiszpanii , na obszarze wspólnoty autonomicznej Andaluzji .
Graniczy na zachodzie z Kordobą , na północy z Ciudad Real , na wschodzie z Albacete , a na południowym wschodzie z Grenadą .
Jej stolicą jest miasto Jaén .
Powierzchnia prowincji wynosi 13 498 km² , a jej liczba ludności 662 751 mieszkańców ( 2006 ) .
Jedna szósta część ludności prowincji zamieszkuje stolicę i jej obszar metropolitarny .
Gęstość zaludnienia wynosi 49,09 os./km² .
Jaén składa się z 97 municypiów ( gmin ) , z których najważniejsze , obok stolicy , to Linares , Úbeda , Andújar , Martos i Alcalá la Real , które mają ponad 20 000 mieszkańców .
Klimat , który można zaklasyfikować do typu śródziemnomorskiego z różnymi podtypami ( subtropikalny , łagodny i kontynentalny ) , znajduje się pod całkowitym wpływem Doliny Gwadalkiwiru , która otwarta na Atlantyk warunkuje cyrkulację atmosferyczną prowincji .
