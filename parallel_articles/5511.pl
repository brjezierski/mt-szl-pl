Janikowo ( gmina Kruszwica )
Janikowo – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie inowrocławskim , w gminie Kruszwica .
Obecnie mieszka w niej 32 mieszkańców .
Jest częścią sołectwa Sokolniki .
Janikowo znajduje się na szlaku piastowskim łączącym Kruszwicę i Strzelno .
Historia .
Nieopodal miejscowości odkryto ślady osady z okresu wpływów rzymskich .
Wieś niegdyś nazywała się Jankowo .
Pracowali tu chłopi zajmujący się rolą i hodowlą świń .
Około 1560 roku wieś należała do pani Oborskiej , nieco później Andrzeja Janickiego .
Kolejny upadek Jankowa miał miejsce w 1665 roku w wyniku wojny polsko-szwedzkiej .
W 1938 roku w miejscowości wybuchł pożar , który strawił zabudowania gospodarskie .
Po II wojnie światowej mieszkańcy otrzymali ziemię z rozparcelowanego majątku rodziny Leclerc z Sukowych .
Dawniej wieś liczyła 12 domów i 96 mieszkańców ( obecnie liczy 10 domów i 32 mieszkańców ) .
