Jaślików
Jaślików – wieś w Polsce położona w województwie lubelskim , w powiecie krasnostawskim , w gminie Krasnystaw .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
W miejscowości znajduje się Szkoła Podstawowa im .
Batalionów Chłopskich w Jaślikowie .
Wieś stanowi sołectwo gminy Krasnystaw .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 450 mieszkańców .
