Jaświły ( Białoruś )
Jaświły ( ; ) – chutor na Białorusi , w obwodzie grodzieńskim , w rejonie werenowskim , w sielsowiecie Żyrmuny , przy linii kolejowej Lida – Bieniakonie .
Dawniej zaścianek .
W dwudziestoleciu międzywojennym leżały w Polsce , w województwie nowogródzkim , w powiecie lidzkim , w gminie Żyrmuny .
