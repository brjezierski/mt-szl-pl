Jasieniec-Maziarze
Jasieniec-Maziarze – wieś w Polsce położona w województwie mazowieckim , w powiecie radomskim , w gminie Iłża .
Obok miejscowości przepływa Małyszyniec , niewielka rzeka , dopływ Iłżanki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa radomskiego .
Wierni kościoła rzymskokatolickiego należą do parafii Najświętszej Maryi Panny Różańcowej w Jasieńcu Iłżeckim .
