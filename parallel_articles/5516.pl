Jasionowo ( rejon brasławski )
Jasionowo – dawny zaścianek .
Tereny , na których był położony , leżą obecnie na Białorusi , w obwodzie witebskim , w rejonie brasławskim .
Historia .
W czasach zaborów zaścianek leżał w granicach Imperium Rosyjskiego .
W latach 1921–1945 folwark a następnie zaścianek leżał w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie brasławskim , w gminie Plusy .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 6 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w jednym domu zamieszkiwało 14 osób .
Miejscowość należała do parafii rzymskokatolickiej w Rymszanach .
Podlegała pod Sąd Grodzki w m. Turmont i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Rymszanach .
