Jean de La Fontaine
Jean de La Fontaine ( ur .
8 lipca 1621 w Château-Thierry , zm .
13 kwietnia 1695 w Paryżu ) – jeden z czołowych przedstawicieli klasycyzmu francuskiego , autor blisko 250 bajek .
Życiorys .
W młodości uczył się mało , dopiero w 22 roku życia zaczął studiować poetów klasycznych .
Porzucił pracę i żonę , po czym udał się do Paryża , gdzie znalazł opiekę między innymi nadintendenta Nicolasa Fouqueta i pani Sablière , która troszczyła się o wszystkie jego potrzeby .
W 1684 został członkiem Akademii Francuskiej .
Sławę literacką zjednał sobie swymi `` Contes '' ( 1665 ) i `` '' ( pierwsze sześć ksiąg 1668 , następnych pięć 1678 , a dwunasta 1694 ; z późniejszych edycji najlepsza Nodiera , 2 t. , Paryż , 1839 ) , których zalety nie tyle polegają na sztuce wynalazczej , ile na naturalnym i prostym wykładzie .
Treść do bajek czerpał z pisarzy starożytnych , a do powiastek z nowelistów włoskich i ze starofrancuskich fabliaux .
Naiwność , prawda , prostota , naturalność i żywość to przymioty , które zapewniły twórczości La Fontaine ' a uznanie .
Najlepiej wszystkie pisma jego `` Oeuvres '' wydał Valekenaer ( 18 t. , Paryż , 1819–20 ; nowa edycja 6 t. , 1822–23 ) oraz Girard i Desfeuilles w dziele `` Grands écrivains '' ( 1880 i nast .
t. 8 ) .
Por .
Saint-Mare Girardin `` L. et les fabulistes '' ( Paryż , 1867 , t. 2 ) ; Taine `` L. et ses fables '' ( 10 wyd. , 1885 ) .
