Życiorys .
Jean-Auguste-Dominique Ingres był synem malarza Josepha Ingres ’ a , od którego przejął zamiłowanie do malarstwa i muzyki ( grał na skrzypcach ) .
W 1791 roku wyjechał do Tuluzy , by tam uczyć się w szkole sztuk pięknych .
Sześć lat później trafił do paryskiej pracowni Jacques-Louis Davida , a w 1801 roku za obraz `` Posłowie Agamemnona '' otrzymał nagrodę Prix de Rome ( wyjazd na studia na Akademii Francuskiej w Rzymie ) .
Udało mu się wyjechać dopiero w 1806 roku .
W tym samym roku jego obrazy , wysłane na paryski Salon , spotkały się z surową krytyką .
Po czteroletnim stypendium nie wrócił do Francji , ale pozostał w Rzymie aż do 1820 roku , po czym przeniósł się do Florencji , gdzie przebywał do 1824 .
W tym też czasie ( w 1813 ) ożenił się z Madeleine Chapelle , która była dla niego podporą w trudnym czasie biedy i braku uznania .
Sytuacja odmieniła się w 1824 roku ; po okresie krytyki płynącej z Francji , Ingres zyskał uznanie i wrócił do Paryża .
