Jean-Baptiste Say
Jean-Baptiste Say ( ur .
5 stycznia 1767 w Lyonie , zm .
15 listopada 1832 w Paryżu ) – francuski przedsiębiorca i ekonomista , przedstawiciel francuskiego nurtu ekonomii klasycznej .
Najpowszechniej znanym dokonaniem Saya jest sformułowanie prawa rynków , zwanego też często wprost `` prawem Saya '' .
Kryjąca się za nim koncepcja była już wcześniej formułowana przez innych ekonomistów , ale Say po raz pierwszy sformułował ją w sposób precyzyjny i naukowy .
W najogólniejszej postaci prawo rynków Saya sprowadza się do tego , że podaż sama stwarza popyt ( zakłada podażowy charakter gospodarki narodowej – popyt jest bierny i zawsze dostosowuje się do podaży ) .
Wynika to z faktu , że koszty produkcji ponoszone przez producentów są jednocześnie zapłatą dla pracowników i podwykonawców , a tym samym są przeznaczone na konsumpcję .
Zakłada się tu , że oszczędności gromadzone przez podmioty są niczym innym jak odroczoną konsumpcją .
Say zakłada też `` implicite '' , że rynek działa w warunkach wolnej konkurencji .
W takiej sytuacji kryzys nadprodukcji może wystąpić jedynie w obliczu czynników zewnętrznych , np .
klęsk żywiołowych .
Prawo rynków Saya wywołało bardzo wiele dyskusji wśród późniejszych ekonomistów , stając się jednym z podstawowych postulatów nurtu klasycznego w ekonomii .
