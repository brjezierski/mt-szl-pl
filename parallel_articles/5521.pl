Jean-Claude Duvalier
Jean-Claude Duvalier , znany jako Baby Doc ( ur .
3 lipca 1951 w Port-au-Prince , zm .
4 października 2014 tamże ) – haitański polityk , prezydent Haiti w latach 1971–1986 , sprawujący władzę dyktatorską .
Życiorys .
Urodzony w 1951 roku .
Od dzieciństwa miał skłonności do tycia i był nazywany przez kolegów szkolnych „ kapuścianą głową ” .
Jedyny syn François i Simone Duvalierów .
François Duvalier w 1971 przeżył wylew krwi do mózgu , po którym , na pół sparaliżowany , rozpoczął przygotowania do przekazania władzy synowi .
31 stycznia 1971 odbyło się referendum , w którym 2 391 916 głosujących ( 100 % ) poparło Jeanem-Claude ' a jako następcę ojca .
François Duvalier wprowadził również zmiany w konstytucji , obniżając minimalny , umożliwiający objęcie prezydentury wiek z 40 do 24 lat oraz zmieniając datę urodzenia Jeana-Claude ' a , który dzięki temu „ zyskał ” 3 lata .
