Jean-Louis Borloo
Jean-Louis Borloo ( ur .
7 kwietnia 1951 w Paryżu ) – francuski polityk i prawnik , parlamentarzysta i minister .
Lider Partii Radykalnej oraz Unii Demokratów i Niezależnych .
Od 2005 żonaty z Béatrice Schönberg .
Życiorys .
Studiował filozofię , nauki ekonomiczne , prawo i historię .
Ukończył studia podyplomowe Master of Business Administration na Uniwersytecie w Manchesterze .
Praktykował jako adwokat w Paryżu .
Był prezesem klubu piłkarskiego Valenciennes FC .
W latach 1989–1992 zasiadał w Parlamencie Europejskim .
Od 1989 do 2002 zajmował stanowisko burmistrza Valenciennes .
Od 1993 w każdych kolejnych wyborach do 2012 włącznie uzyskiwał mandat deputowanego do francuskiego Zgromadzenia Narodowego .
Również w wyborach parlamentarnych w 2012 uzyskał poselską reelekcję .
Zaangażował się w działalność socjalliberalnej Partii Radykalnej , był m.in .
jej współprzewodniczącym ( 2005–2007 ) , następnie został prezydentem tego ugrupowania .
Wraz z radykałami należał do Unii na rzecz Demokracji Francuskiej , następnie nawiązał współpracę Unii na rzecz Ruchu Ludowego , w ramach której PR uzyskała status partii afiliowanej .
Od 2004 był ministrem do spraw polityki społecznej w rządach Jean-Pierre ’ a Raffarina i Dominique ’ a de Villepin .
18 maja 2007 objął urząd ministra finansów i gospodarki w pierwszym gabinecie François Fillona .
19 czerwca w drugim rządzie tego premiera , powołanym po wyborach parlamentarnych , został ministrem stanu ( do spraw energii , ekologii , transportu i zrównoważonego rozwoju ) .
Wprowadził pięcioletni plan polityki społecznej , opierający się na trzech celach „ równej godności , zamieszkania i zatrudnienia ” .
Z rządu odszedł po rekonstrukcji dokonanej jesienią 2010 .
Po wyborach w 2012 zainicjował powołanie nowej centrowej grupy parlamentarnej , a następnie federacyjnej partii politycznej pod nazwą Unia Demokratów i Niezależnych .
W kwietniu 2014 z powodów zdrowotnych związanych z hospitalizacją wynikającą z zapalenia płuc złożył wszystkie funkcje polityczne i partyjne , rezygnując m.in .
z mandatu deputowanego .
