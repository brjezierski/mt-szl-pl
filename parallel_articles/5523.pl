Jean-Michel Jarre
Jean-Michel André Jarre ( ur .
24 sierpnia 1948 w Lyonie ) – francuski kompozytor , performer i producent muzyczny , pionier muzyki elektronicznej , ambientu i new age , znany również z organizowania koncertów-spektakli , wykorzystujący na wielką skalę efekty świetlne , pokazy laserowe i sztuczne ognie .
Z muzyką miał do czynienia od najmłodszych lat – jego ojciec i dziadek byli muzykami , on sam pobierał lekcje gry na fortepianie , obserwował ulicznych wykonawców czy muzyków jazzowych .
Grał na gitarze w zespole , ale na jego styl muzyczny największy wpływ miał prawdopodobnie Pierre Schaeffer , prekursor muzyki konkretnej .
Jego pierwszym międzynarodowym sukcesem był album `` Oxygène '' z 1976 , nagrany w domowym studiu .
W 1978 ukazał się album `` Équinoxe '' , utrzymany w podobnym stylu , który ugruntował popularność Jarre ’ a .
Koncert na Place de la Concorde w Paryżu z 1979 , wysłuchany przez ponad milion osób , ustanowił rekord ( odnotowany w księdze rekordów Guinnessa ) , który Jarre pobił później jeszcze trzykrotnie .
Do 2004 artysta sprzedał około 80 mln swoich albumów .
