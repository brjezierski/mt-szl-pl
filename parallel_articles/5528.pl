Jeff Gordon
Jeffrey Michael „ Jeff ” Gordon ( ur .
4 sierpnia 1971 w Vallejo ) – amerykański kierowca wyścigowy , a obecnie komentator sportowy stacji Fox .
Jest czterokrotnym mistrzem NASCAR Winston Cup ( obecnie Monster Energy NASCAR Cup Series ) , trzykrotnym zwycięzcą Daytona 500 i triumfatorem aż 93 wyścigów w najwyższych rangą rozgrywkach NASCAR ( co daje mu trzecie miejsce w rankingu najczęściej wygrywających kierowców w historii owej serii ) .
Przez całą swoją karierę w NASCAR Cup Series związany był z zespołem Hendrick Motorsports .
Od pierwszego startu w 1992 roku aż do odejścia na emeryturę z końcem sezonu 2015 wziął udział w 797 wyścigach ( jest dzięki temu rekordzistą w ilości wyścigów zaliczonych pod rząd ) , w których kierował Chevroletem z numerem startowym 24 .
W drugiej połowie 2016 roku wystartował w kolejnych ośmiu wyścigach dla tego samego zespołu w samochodzie nr 88 , w którym zastępował kontuzjowanego Dale ’ a Earnhardta Jr .
