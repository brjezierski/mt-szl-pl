Jegliniszki ( jezioro )
Jegliniszki – jezioro położone w północno-wschodniej Polsce , w województwie podlaskim , w powiecie suwalskim , w gminie Wiżajny .
Jezioro Jegliniszki jest płytkim , powoli zarastającym zbiornikiem wodnym .
Latem rośliny pływające pokrywają sporą część tafli jeziora .
Są to głównie grążele żółte i grzybienie północne ( nenufary ) .
Nad jeziorem można zaobserwować liczne ptactwo wodne , przede wszystkim różne gatunki kaczek .
Z południowo-zachodniego brzegu jeziora wypływa ciek wodny , zasilający Jezioro Hańcza .
