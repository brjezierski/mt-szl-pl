Jegor Gajdar
Jegor Timurowicz Gajdar ( ros .
`` Егор Тимурович Гайдар '' ) ( ur .
19 marca 1956 w Moskwie , zm .
16 grudnia 2009 tamże ) – premier i minister finansów Federacji Rosyjskiej w latach 1991–1992 .
Życiorys .
Wnuk pisarza Arkadego Gajdara , syn admirała Timura Gajdara .
W 1978 ukończył studia na wydziale ekonomii Uniwersytetu Moskiewskiego .
Od 1980 był członkiem KPZR i redaktorem działu gospodarczego dziennika KC KPZR `` Kommunist '' .
W 1990 objął kierownictwo działu gospodarczego `` Prawdy . ''
W 1991 opuścił szeregi partii komunistycznej i został doradcą Borysa Jelcyna .
Jako minister finansów był jednym z autorów terapii szokowej , która poprzez uwolnienie cen spowodowała gwałtowną drożyznę i drastycznie pogorszyła poziom życia ludności .
Udało mu się jednak w znacznym stopniu zredukować deficyt budżetowy .
W 1991 został ministrem rozwoju gospodarczego .
Od 15 czerwca do 14 grudnia 1992 pełnił obowiązki premiera Rosji .
Od września 1993 do stycznia 1994 był pierwszym wicepremierem .
W 1994 założył partię Demokratyczny Wybór Rosji .
W 1999 z Anatolijem Czubajsem został współprzewodniczącym Sojuszu Sił Prawicowych .
W latach 1999–2003 był deputowanym do Dumy Państwowej .
W 2001 doprowadził do połączenia Demokratycznego Wyboru Rosji z Sojuszem Sił Prawicowych .
Był autorem ponad 100 publikacji naukowych z dziedziny ekonomii .
Był nieprzejednanym krytykiem polityki Władimira Putina , zarzucał mu doprowadzenie do odejścia Rosji od systemu gospodarki wolnorynkowej .
24 listopada 2006 został prawdopodobnie podtruty w czasie swojej wizyty w Irlandii , gdzie promował swoją nową książkę `` The Death of Empire : Lessons for Contemporary Russia '' ( pol .
`` Zgon imperium – nauka dla współczesnej Rosji '' ) .
Komentatorzy wiążą ten fakt z niewyjaśnionymi zabójstwami Anny Politkowskiej i Aleksandra Litwinienki .
W nocy z 15 na 16 grudnia 2009 , w wieku 53 lat , zmarł w Moskwie .
Przyczyną były problemy z krążeniem .
Jego córka Maria Gajdar , w 2015 roku dostała ukraińskie obywatelstwo i została mianowana zastępcą gubernatora Odessy ( M. Saakaszwilego ) .
