Jekaterynburg
Jekaterynburg ( , `` Jekatierinburg '' ) – miasto w azjatyckiej części Rosji położone po wschodniej stronie środkowego Uralu , w obwodzie swierdłowskim , nad rzeką Iset .
W latach 1924–1991 nosiło nazwę Swierdłowsk ( ) na cześć bolszewickiego przywódcy Jakowa Swierdłowa .
Dane ogólne .
Jekaterynburg jest czwartym pod względem liczby mieszkańców miastem Rosji ( 1 495 066 mieszkańców ) .
Jest stolicą obwodu swierdłowskiego w Uralskim Okręgu Federalnym .
Stanowi ważny ośrodek gospodarczy i kulturalno-naukowy kraju .
Miasto jest ośrodkiem przemysłu elektromaszynowego , hutniczego , chemicznego , skórzanego , włókienniczego , spożywczego i materiałów budowlanych .
Na terenie miasta działają 54 szpitale .
Jest ważnym ośrodkiem naukowym ; mieszczą się tutaj Uralski Uniwersytet Federalny i inne szkoły wyższe , instytuty naukowo-badawcze , Uralski Oddział Rosyjskiej Akademii Nauk .
W mieście znajduje się nowoczesny kompleks biznesowy Antey .
Najbardziej znaną osobą urodzoną w mieście był Borys Jelcyn .
W 1918 r. , po przewrocie bolszewickim , więziono tu i zamordowano cara Mikołaja II wraz z rodziną .
Historia .
Początki .
Najwcześniejsze ślady osadnictwa na terenie , który obecnie zajmuje Jekaterynburg , pochodzą z okresu mezolitu , to jest z VIII i VII tysiąclecia p.n.e .
Tereny te były zasiedlane także w neolicie , chalkolicie oraz epokach brązu , miedzi i żelaza .
Z uwagi na swoje położenie u podnóża Uralu nie były ziemie te najatrakcyjniejszym terenem pod osadnictwo .
