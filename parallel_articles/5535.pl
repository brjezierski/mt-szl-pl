Jełgawa
Jełgawa ( łot .
`` Jelgava '' ; do 1918 Mitawa , niem .
`` Mitau '' , ros .
Елгава , jidysz מיטאַווע , `` Mitawe '' ) – miasto na Łotwie , w środkowej części kraju , ok. 45 km na południowy zachód od Rygi , przy ujściu rzeki Drykszny do Lelupy ( dawniej Aa ) , miasto wydzielone .
W mieście znajduje się stacja kolejowa Jelgava .
Historia .
Na terenie obecnej Jełgawy w X wieku powstała osada Liwów .
W XIII wieku , inflancka gałąź zakonu krzyżackiego , pod wodzą mistrza krajowego Konrada von Mandera rozpoczęła budowę w tym miejscu zamku , nazwanego `` Mitau '' .
