Jelnica ( rejon miński )
Jelnica ( biał .
`` Ельніца '' , ros .
`` Ельница '' ) – wieś na Białorusi , w obwodzie mińskim , w rejonie mińskim , w sielsowiecie Nowy Dwór .
Od północy graniczy z Mińskiem .
W czasach Rzeczypospolitej ziemie te leżały w województwie mińskim .
Odpadły od Polski w wyniku II rozbioru .
W granicach Rosji wieś należała do ujezdu ihumeńskiego w guberni mińskiej .
Ponownie pod polską administracją w latach 1919 - 1920 w okręgu mińskim Zarządu Cywilnego Ziem Wschodnich .
