Jeonju
Jeonju ( kor .
전주시 , `` Jeonju-si '' ) – miasto i stolica prowincji Jeolla Północna w Korei Południowej .
Położone 3 godziny drogi na południe od Seulu .
Ważny ośrodek turystyczny , znany z doskonałej kuchni , zabytków , festiwali i imprez sportowych .
Siedziba rzymskokatolickiej diecezji Jeonju .
Historia .
Położone na żyznej równinie Honam , Jeonju grało od dawna ważną rolę centrum regionalnego w prowincji .
Miasto zyskało miano `` miasta królewskiego '' , kiedy założyciel dynastii Yi ( Joseon ) , generał Yi Seong-gye pokonał najeżdżające Koreę wojska japońskie w 1302 roku .
W 1894 roku miasto zostało zajęte przez chłopskich rebeliantów Donghak .
Prawa miejskie Jeonju uzyskało w 1949 roku .
