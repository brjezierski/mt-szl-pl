Jercewo
Jercewo ( ros .
Ерцево ) – osiedle typu miejskiego w obwodzie archangielskim .
Znajduje się tam drewniana cerkiew pw .
Matki Boskiej Kazańskiej .
W ZSRR położony w pobliżu Archangielska nad Morzem Białym jeden z radzieckich obozów pracy niewolniczej archipelagu Gułag .
Został założony prawdopodobnie w 1936 roku .
W latach 1938-1940 przebywał w nim malarz i grafik Wiktor Toot .
W 1940 roku stał się częścią kompleksu obozowego w Kargopolu , w którym przebywało ok. 300 000 więźniów .
Jercewo było głównym ośrodkiem przemysłu drzewnego z własnym dworcem i urzędem we wsi .
W pobliskim lesie znajdowały się prymitywne budy , w tym : barak szpitalny dla 600 więźniów , `` trupiarnia '' , biblioteka i barak tranzytowy .
Jercewo służyło jako obóz przejściowy , jednak wielu już na tym etapie umierało .
