Jerez de la Frontera
Jerez de la Frontera – miasto w południowo-zachodniej Hiszpanii , w Andaluzji , w prowincji Kadyks .
Ludność : 215,1 tys .
mieszkańców ( 2013 ) .
Miasto jest znane z produkcji sherry oraz ze stadnin koni .
W mieście znajduje się stacja kolejowa Jerez de la Frontera oraz tor Formuły 1 Circuito Permanente de Jerez , na którym kierowcy często testują bolidy .
Historia .
Nazwa miasta pochodzi od fenickiej osady `` Xera '' , zromanizowanej później jako `` Ceret '' oraz `` Sèrès '' .
Dokładna lokalizacja pierwotnej miejscowości nie jest znana .
Po okresie panowania rzymskiego przekształciła się w ośrodek wizygocki , by wraz z najazdem arabskim stać się świadkiem zagłady państwa Wizygotów .
W pobliżu Jerez de la Frontera bowiem wojska króla Roderyka poniosły w bitwie nad rzeką Guadelete w 711 roku miażdżącą porażkę w starciu z ponad czterokrotnie mniej liczebnymi oddziałami arabskimi pod dowództwem Tarik ibn Ziyada .
Pod panowaniem arabskim miasto było znane jako `` Scherisch '' .
Miasto ponownie znalazło się w rękach chrześcijan w 1264 roku za sprawą Alfonsa X Mądrego , a jego nazwę sprowadzono do `` Xeres '' lub `` Xerez '' .
Od XVI wieku miasto intensywnie rozwijało się za sprawą wzrostu popularności wytwarzanego tu wina – sherry .
