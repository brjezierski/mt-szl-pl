Jerozolima ( muhafaza )
Jerozolima ( , `` Al-Quds '' ) – muhafaza Palestyny .
Leży w środkowej części Zachodniego Brzegu .
Od północy sąsiaduje z Jerychem i Ramallah i Al-Bira , a od południa z Betlejem .
Od wschodu ma dostęp do Morza Martwego , zaś od zachodu graniczy izraelskim dystryktem Jerozolima .
Ma powierzchnię 345 km² i jest trzecią co do wielkości jednostką administracyjną kraju .
Według danych Palestyńskiego Centralnego Biura Statystycznego na rok 2007 zamieszkało ją 362 521 osób , co stanowiło 9,6 % ludności Palestyny .
Znajdowało się tu wtedy 70 308 gospodarstw domowych .
Do 2015 roku liczba ludności wzrosła do 419 108 , a gęstość zaludnienia wynosiła 1 215 os./km² .
Jest to trzecia pod względem liczby ludności muhafaza Palestyny .
