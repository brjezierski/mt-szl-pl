Jersey City
Jersey City – miasto w Stanach Zjednoczonych , w stanie New Jersey ( siedziba hrabstwa Hudson ) , na półwyspie między ujściami rzek Hudson i Hackensack do Oceanu Atlantyckiego .
Według spisu ludności z roku 2010 , w Jersey City mieszkało 247 597 mieszkańców .
Miasto to nazywane jest potocznie `` Sixth borough '' ( `` Szóstą dzielnicą '' Nowego Jorku ) .
W granicach administracyjnych Jersey City znajduje się wyspa Liberty Island ze słynną Statuą Wolności .
W mieście tym znajduje się Pomnik Katyński autorstwa Andrzeja Pityńskiego .
