Jersey ( rasa bydła )
Jersey , dżersej – rasa bydła typu mlecznego wyhodowana na wyspie Jersey .
Ze względu na 200-letnią izolację rasa jest bardzo ujednolicona genetycznie .
Charakteryzuje się zawartością tłuszczu w mleku dochodzącą do 7 % .
Umaszczenie od jasnocielistego do brunatnoczerwonego , jednolite ale możliwe białe łaty .
Budowa bardzo delikatna , drobna .
Masa ciała :
Wysokość w kłębie około 145 cm .
Rasa ta ma dużą pojemność wymienia .
Wydajność mleczna wynosi 4500 kg .
Do Polski sprowadzana od 1960 roku .
