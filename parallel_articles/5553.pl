Jerzy Brzęczek
Jerzy Józef Brzęczek ( ur .
18 marca 1971 w Truskolasach ) – polski piłkarz i trener piłkarski , reprezentant Polski , srebrny medalista olimpijski z 1992 .
W latach 2018–2021 selekcjoner reprezentacji Polski w piłce nożnej .
Kariera .
Kariera klubowa .
Treningi rozpoczął w Olimpii Truskolasy , następnie został graczem Rakowa Częstochowa .
Potem grał kolejno w Olimpii Poznań , Lechu Poznań ( mistrzostwo Polski ) , Górniku Zabrze , GKS Katowice , Tirolu Innsbruck ( dwukrotnie mistrzostwo Austrii ) , LASK Linz , Maccabi Hajfa , Sturmie Graz , FC Kärnten oraz Wackerze Innsbruck .
Latem 2007 , po dwunastu latach gry poza krajem , podpisał roczny kontrakt z Górnikiem Zabrze , który 15 kwietnia 2008 przedłużono o kolejny rok .
W styczniu 2009 rozwiązał kontrakt z klubem i na zasadzie wolnego transferu przeszedł do Polonii Bytom , gdzie był grającym asystentem trenera Marka Motyki .
Latem 2009 zakończył karierę .
Kariera reprezentacyjna .
W seniorskiej reprezentacji Polski A zadebiutował w 1992 .
Rozegrał w niej 42 oficjalne spotkania międzypaństwowe , zdobywając cztery bramki .
W kilku meczach pełnił funkcję kapitana drużyny narodowej .
Był również kapitanem kadry olimpijskiej , która w 1992 na Letnich Igrzyskach w Barcelonie wywalczyła srebrny medal .
Kariera trenerska .
Na początku lutego 2010 został pierwszym trenerem Rakowa Częstochowa .
Po serii nieudanych meczów umowa z klubem została rozwiązana 3 listopada 2014 , na prośbę głównego sponsora .
17 listopada 2014 objął funkcję trenera Lechii Gdańsk , a 1 września 2015 został zwolniony .
Następnie od końca września 2015 był szkoleniowcem GKS Katowice .
20 maja 2017 , po przegranym meczu z MKS Kluczbork i utracie szans awansu do ekstraklasy , podał się do dymisji .
11 lipca 2017 został trenerem Wisły Płock .
12 lipca 2018 ogłoszono go selekcjonerem reprezentacji Polski .
Oficjalna prezentacja trenera odbyła się 23 lipca 2018 , a 1 sierpnia 2018 faktycznie objął on tę funkcję .
W jego selekcjonerskim debiucie , 7 września 2018 „ biało-czerwoni ” zremisowali 1:1 w Bolonii z Włochami w inauguracyjnym spotkaniu pierwszej edycji Ligi Narodów UEFA .
18 stycznia 2021 został zwolniony ze stanowiska selekcjonera .
Ponad rok później , a dokładniej 14 lutego 2022 objął stanowisko trenera Wisły Kraków .
W jego pierwszym sezonie , Wisła spadła z Ekstraklasy .
1 czerwca 2022 roku poinformowano o przedłużeniu kontaktu przez klub z trenerem do końca czerwca 2023 , z opcją przedłużenia o 12 miesięcy w przypadku awansu z pierwszej ligi .
3 października 2022 przestał pełnić funkcję trenera krakowskiej Wisły .
Kariera medialna .
Był ekspertem Polsatu podczas Mistrzostw Europy 2008 , następnie komentował mecze Ekstraklasy w Canal+ Sport .
W 2022 został ekspertem TVP Sport na Mistrzostwa Świata w Katarze .
