Jerzy Dudek
Jerzy Henryk Dudek ( ur .
23 marca 1973 w Rybniku ) – polski piłkarz , który występował na pozycji bramkarza .
W latach 1998–2013 reprezentant Polski .
Uczestnik Mistrzostw Świata 2002 .
Członek Klubu Wybitnego Reprezentanta , zajmuje trzecie miejsce pod względem liczby występów bramkarzy w reprezentacji Polski .
Po występach w lokalnych drużynach w 1995 trafił do Sokoła Tychy , gdzie zadebiutował w pierwszej lidze .
Rok później trafił do Feyenoordu , gdzie w ciągu pięciu sezonów zdobył mistrzostwo Holandii i dwukrotnie został uznany za najlepszego bramkarza Eredivisie .
W 2000 został wybrany piłkarzem roku w Polsce w plebiscycie „ Piłki Nożnej ” .
W 2001 trafił do Liverpoolu , gdzie spędził sześć kolejnych sezonów i sięgnął z klubem po trofeum Ligi Mistrzów UEFA , zostając jednym z bohaterów meczu finałowego w Stambule .
W 2007 przeniósł się do Realu Madryt , gdzie jako drugi bramkarz spędził cztery sezony , zdobywając wszystkie krajowe trofea i debiutując w Primera Division .
Jest jednym z sześciu polskich piłkarzy – zdobywców Pucharu Europy .
Kariera piłkarska .
Concordia Knurów .
Karierę rozpoczął jesienią 1985 w trampkarzach Górnika Knurów , trenował w drużynie Witolda Słabkowskiego .
Podczas jednego z sezonów rozegrał w nich 12 spotkań jako lewy obrońca .
Wiosną 1989 zadebiutował w drużynie juniorów Górnika , grając w meczu z GKS Tychy .
W 1991 trafił do pierwszego zespołu klubu , występującego w III lidze ; w drużynie , grającej pod nową nazwą – Concordia Knurów , zadebiutował 10 kwietnia 1992 w meczu z Carbo Gliwice .
W barwach Concordii rozegrał 115 spotkań , ostatni pojedynek zanotował przeciwko Olimpii Piekary Śląskie w listopadzie 1995 .
Sokół Tychy .
Wiosną 1996 za sprawą trenera Bogusława Kaczmarka przeszedł do Sokoła Tychy , występującego w ekstraklasie .
Debiut w ekstraklasie zaliczył 16 marca 1996 w przegranym 2:0 meczu wyjazdowym z Legią Warszawa .
Po rozegraniu 15 meczów ligowych odszedł z klubu .
Feyenoord Rotterdam .
19 lipca 1996 podpisał profesjonalny kontrakt z holenderskim klubem występującym w lidze Eredivisie – Feyenoordem .
Podczas sezonu 1996/1997 był bramkarzem rezerwowym , gdyż podstawowym bramkarzem był reprezentant Holandii – Ed de Goey .
Po jego odejściu do występującego w angielskiej Premier League – Chelsea Londyn Dudek został podstawowym zawodnikiem klubu , w którego barwach zadebiutował 13 sierpnia 1997 w wygranym 6:2 pierwszym meczu II rundy kwalifikacyjnej Ligi Mistrzów 1997/1998 z fińskim Jazz Pori .
