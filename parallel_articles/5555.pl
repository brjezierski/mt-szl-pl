Jerzy Gorzelik
Jerzy Zdzisław Gorzelik ( ur .
25 października 1971 w Zabrzu ) – historyk sztuki , nauczyciel akademicki , profesor Uniwersytetu Śląskiego w Katowicach , górnośląski działacz regionalny i samorządowy .
Od 2003 Przewodniczący Ruchu Autonomii Śląska , w latach 2010–2013 Członek Zarządu Województwa Śląskiego IV kadencji , a w 2010–2018 radny Sejmiku Województwa Śląskiego IV i V kadencji .
Życiorys i działalność naukowa .
Wykształcenie .
Absolwent I Liceum Ogólnokształcącego im .
Mikołaja Kopernika w Katowicach .
W 1995 ukończył historię sztuki na Wydziale Historycznym Uniwersytetu Jagiellońskiego .
23 czerwca 1999 uzyskał stopień doktora nauk humanistycznych w zakresie nauk o sztuce na Wydziale Nauk Historycznych i Pedagogicznych Uniwersytetu Wrocławskiego , na podstawie rozprawy `` Etapy przemian sztuki barokowej w opactwie cysterskim w Rudach Wielkich koło Raciborza '' , której promotorem była prof. dr hab .
Ewa Chojecka .
W 1995 został pracownikiem naukowym Wydziału Nauk Społecznych Uniwersytetu Śląskiego , obejmując stanowisko asystenta , a od 1999 adiunkta w Zakładzie Historii Sztuki .
