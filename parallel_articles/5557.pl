Jerzy Stuhr
Jerzy Oskar Stuhr ( ur .
18 kwietnia 1947 w Krakowie ) – polski aktor teatralny i filmowy , reżyser , filolog i pisarz , profesor sztuk teatralnych .
Uznawany za jednego z najwybitniejszych i najpopularniejszych polskich aktorów .
Laureat Polskiej Nagrody Filmowej za rolę drugoplanową w filmie `` Persona non grata '' ( 2005 ) i nagrody specjalnej za osiągnięcia życia ( 2018 ) .
W latach 1990–1996 oraz 2002–2008 rektor PWST w Krakowie .
Życiorys .
Rodzina i edukacja .
Urodził się 18 kwietnia 1947 w Krakowie jako jedyne dziecko Tadeusza Stuhra i jego żony Marii z domu Chorąży , którzy pobrali się 6 czerwca 1946 .
Jego matka była księgową w Miejskim Handlu Detalicznym oraz w teatrze Banialuka w Bielsku-Białej , a ojciec był prokuratorem .
Pochodzi z rodziny od pokoleń związanej z Krakowem .
Pierwsi jej przedstawiciele – Anna z Thillów oraz Leopold Stuhrowie – przybyli do miasta z powiatu Mistelbach w Dolnej Austrii w 1879 .
Pradziadek aktora , Leopold Stuhr , należał do zamożnego mieszczaństwa ; osiadłszy w Krakowie , kupił kamienicę przy Rynku Podgórskim i założył restaurację .
