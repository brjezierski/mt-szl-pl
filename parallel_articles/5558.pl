Jerzy Szmajdziński
Jerzy Andrzej Szmajdziński ( ur .
9 kwietnia 1952 we Wrocławiu , zm .
10 kwietnia 2010 w Smoleńsku ) – polski polityk , poseł na Sejm PRL IX kadencji w latach 1985–1989 , poseł na Sejm I , II , III , IV , V i VI kadencji ( 1991–2010 ) , wicemarszałek Sejmu VI kadencji , w latach 2001–2005 minister obrony narodowej .
Życiorys .
W latach 1966–1971 był uczniem Elektronicznych Zakładów Naukowych we Wrocławiu .
W 1975 ukończył studia na Akademii Ekonomicznej we Wrocławiu .
W 1968 wstąpił do Związku Młodzieży Socjalistycznej .
Jako student przewodniczył radzie uczelnianej Socjalistycznego Związku Studentów Polskich , potem zasiadał w zarządzie głównym tej organizacji .
W latach 1973–1990 był członkiem Polskiej Zjednoczonej Partii Robotniczej .
Zasiadał w egzekutywie Komitetu Uczelnianego na AE we Wrocławiu , w 1975 był delegatem na Wojewódzką Konferencję Sprawozdawczo Wyborczą partii .
Od 1986 zasiadał w jej Komitecie Centralnym .
W latach 1986–1989 był członkiem Ogólnopolskiego Komitetu Grunwaldzkiego .
W okresie 1984–1989 był przewodniczącym Zarządu Głównego Związku Socjalistycznej Młodzieży Polskiej , a w 1989–1990 kierownikiem Wydziału Organizacyjnego KC PZPR .
W latach 1986–1988 był członkiem Społecznego Komitetu Odnowy Starego Miasta w Zamościu .
Od chwili powołania należał do Socjaldemokracji Rzeczypospolitej Polskiej , pełniąc funkcje we władzach krajowych tej partii .
Od 1999 działał w Sojuszu Lewicy Demokratycznej .
W latach 1988–1990 był wiceprezesem Polskiego Komitetu Olimpijskiego .
