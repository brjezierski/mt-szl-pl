Jerzy Urban
Jerzy Urban , pierwotnie Jerzy Urbach ( ur .
3 sierpnia 1933 w Łodzi , zm .
3 października 2022 w Piasecznie ) – polski dziennikarz , publicysta , satyryk , pisarz i polityk .
W latach 1981–1989 rzecznik prasowy Rady Ministrów PRL , w tym okresie jeden z głównych autorów propagandy państwowej i cenzury .
Od kwietnia do września 1989 minister-członek Rady Ministrów oraz przewodniczący Komitetu do spraw Radia i Telewizji „ Polskie Radio i Telewizja ” .
W latach 1990–2022 redaktor naczelny tygodnika „ NIE ” i właściciel wydającej go spółki Urma .
Życiorys .
Życie i działalność do 1989 .
Urodził się jako syn Jana Urbana , dziennikarza , członka Bundu i Polskiej Partii Socjalistycznej , współwłaściciela dziennika „ Głos Poranny ” oraz Marii z domu Brodacz ( 1900–1994 ) .
Rodzina Urbachów była żydowska , ale niereligijna i zaasymilowana .
Wg Urbana jedna z jego babek była Rosjanką .
Wczesne dzieciństwo spędził w Łodzi .
Mieszkał przy ulicy Piotrkowskiej 134 , w poprzecznej oficynie na pierwszym piętrze .
Jesienią 1939 przeniósł się wraz z rodziną do Lwowa .
Ojciec Jerzego otrzymał tam posadę inżyniera w Państwowym Instytucie Projektowania Miast .
Cała rodzina przyjęła radzieckie obywatelstwo .
Po ataku Niemiec na Związek Radziecki ukrywali się na Kresach Wschodnich .
