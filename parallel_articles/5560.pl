Jerzy Ziętek
Jerzy Jan Antoni Ziętek , ps .
„ Jorg ” ( ur .
10 czerwca 1901 w Szobiszowicach , zm .
20 listopada 1985 w Zabrzu ) – polski polityk , urzędnik , samorządowiec , działacz partyjny , państwowy i społeczny oraz wojskowy .
Generał brygady Sił Zbrojnych Polskiej Rzeczypospolitej Ludowej .
Członek Komitetu Centralnego PZPR ( 1964–1981 ) , przewodniczący prezydium Wojewódzkiej Rady Narodowej w Katowicach ( 1964–1973 ) , wojewoda śląski ( 1945 ) , wojewoda katowicki ( 1973–1975 ) , członek Rady Państwa ( 1963–1985 , w tym od 1980 zastępca przewodniczącego ) .
Poseł na Sejm III kadencji w II RP , na Sejm Ustawodawczy oraz na Sejm PRL II , III , IV , V , VI , VII i VIII kadencji .
Prezes Związku Weteranów Powstań Śląskich , wiceprezes Rady Naczelnej Związku Bojowników o Wolność i Demokrację ( w latach 1949–1985 ) , członek Ogólnopolskiego Komitetu Frontu Jedności Narodu .
Budowniczy Polski Ludowej .
Życiorys .
Młodość i działalność w dwudziestoleciu międzywojennym .
Urodził się w Szobiszowicach pod Gliwicami .
Jego rodzicami byli Antoni i Maria z Urbanków .
W latach 1919–1921 aktywny jako działacz propolski na Górnym Śląsku .
Uczestnik powstań śląskich .
Biografowie Jerzego Ziętka twierdzą jednak , że w I i II powstaniu nie brał udziału , natomiast w III był łącznikiem , lecz nie był bezpośrednio zaangażowany w walki zbrojne .
W 1920 pracował w komisariacie plebiscytowym na powiat gliwicko-toszecki .
W tym samym roku został członkiem Polskiej Organizacji Wojskowej Górnego Śląska .
Jerzy Ziętek udzielał się też w polskich organizacjach , np .
Towarzystwie Gimnastycznym „ Sokół ” .
W 1922 lub 1923 przyjechał do Tarnowskich Gór .
Od 1925 pełnił funkcję sekretarza Wydziału Powiatowego w Tarnowskich Górach .
W czerwcu 1928 został zastępcą naczelnika urzędu stanu cywilnego .
24 stycznia 1929 został nominowany na naczelnika Radzionkowa .
W radzie gminy jego lista uzyskała 19 na 25 głosów .
W kolejnych wyborach do Rady Gminnej z 27 kwietnia 1930 otrzymała już tylko 1 mandat .
W sierpniu tego samego roku został członkiem zarządu powiatowego straży pożarnej w Tarnowskich Górach .
9 lutego 1931 został wybrany na naczelnika Radzionkowa .
Poseł na Sejm III kadencji z ramienia Bezpartyjnego Bloku Współpracy z Rządem w latach 1931–1935 .
II wojna światowa .
Po agresji Niemiec na Polskę został ewakuowany wraz z innymi urzędnikami województwa śląskiego na wschód kraju , na tereny po agresji ZSRR na Polskę okupowane przez ZSRR .
Zamieszkał we Lwowie , gdzie pracował jako portier .
Latem 1940 został deportowany i trafił do obozu pracy w Rybińsku nad Wołgą , gdzie przebywał do jesieni 1941 , kiedy to zwolniony został na mocy amnestii po podpisaniu układu Sikorski-Majski .
Dotarł na Kaukaz , gdzie pracował m.in .
w fabryce octu niedaleko Groznego .
W czerwcu 1943 wstąpił do Polskich Sił Zbrojnych w ZSRR .
Został skierowany do szkoły oficerów polityczno-wychowawczych w Riazaniu .
Objął stanowisko zastępcy dowódcy 3 Dywizji Piechoty im .
Romualda Traugutta .
Działalność po II wojnie światowej .
Do Katowic wrócił w styczniu 1945 już w randze podpułkownika .
