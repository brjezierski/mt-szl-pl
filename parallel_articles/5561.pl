Jesiotrowate
Jesiotrowate ( Acipenseridae ) – rodzina słodkowodnych lub anadromicznych ryb jesiotrokształtnych ( Acipenseriformes ) .
Poławiane gospodarczo dla mięsa i kawioru .
Zasięg występowania .
Chłodne i umiarkowanie ciepłe wody półkuli północnej .
Znane z zapisów kopalnych od późnej kredy .
Wraz z wiosłonosowatymi są jedynymi żyjącymi przedstawicielami ryb kostnochrzęstnych .
Cechy morfologiczne .
Charakteryzują się silnie wydłużonym , wrzecionowatym ciałem , o pięciokątnym kształcie w przekroju poprzecznym .
Skóra pokryta pięcioma rzędami tarczek kostnych ( grzbietowy , dwa boczne i dwa brzuszne ) .
Masywna głowa wyciągnięta w ryj ( tzw .
`` rostrum '' - służący do rycia w podłożu ) , całkowicie pokryta kostnymi płytkami .
Otwór gębowy położony w spodniej części głowy , zakończony wysuwalnymi szczękami , poprzedzony dwiema parami wąsików .
Krótka płetwa grzbietowa przesunięta do tyłu ponad płetwę odbytową .
Heterocerkalna płetwa ogonowa z wysokim i długim płatem górnym .
Pierwszy promień płetw piersiowych przekształcony w kolec .
Duży pęcherz pławny połączony z przełykiem ( z wyjątkiem nibyłopatonosów , u których brak pęcherza pławnego ) .
Dorosłe osobniki nie mają zębów .
Ubarwienie zmienne .
Największe osobniki osiągają ponad 4 m długości ( najdłuższa bieługa miała 7,2 m i wagę 1571 kg , najcięższa ważyła 2072 kg ) .
Gatunki słodkowodne są znacznie mniejsze .
Tryb życia .
Tarło odbywają w rzekach .
Ikra o średnicy ziarna ok. 3 mm składana jest w dużych ilościach ( kilkaset tysięcy do kilku milionów sztuk ) .
Większość gatunków wędruje do mórz , tylko nieliczne pozostają w wodach słodkich ( łopatonosy i niektóre formy jesiotrów ) .
Żywią się głównie bezkręgowcami , a starsze osobniki również rybami .
Żerują głównie przy dnie .
