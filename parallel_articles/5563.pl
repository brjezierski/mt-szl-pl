Jessentuki
Jessentuki ( ros .
Ессентуки ) – miasto w europejskiej części Rosji , w Kraju Stawropolskim , na przedgórzu Wielkiego Kaukazu na wysokości 640 m n.p.m .
Liczy 114 824 mieszkańców ( 2021 ) .
Założone w 1836 roku .
Od 1917 roku posiada prawa miejskie .
Jessentuki są znanym rosyjskim kurortem balneologicznym .
