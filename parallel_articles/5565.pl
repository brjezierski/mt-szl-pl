Jewgienij Płatow
Jewgienij Arkadjewicz Płatow , ros .
Евгений Аркадьевич Платов ( ur .
7 sierpnia 1967 w Odessie ) – rosyjski łyżwiarz figurowy , startujący w parach tanecznych z Oksaną Griszczuk .
Dwukrotny mistrz olimpijski z Lillehammer ( 1994 ) i z Nagano ( 1998 ) , uczestnik igrzysk olimpijskich ( 1992 ) , czterokrotny mistrz świata ( 1994–1997 ) , trzykrotny mistrz Europy ( 1996–1998 ) , trzykrotny mistrz świata juniorów ( 1984–1986 ) , mistrz Związku Radzieckiego ( 1992 ) oraz dwukrotny mistrz Rosji ( 1993 , 1996 ) .
