Historia .
Eupatoria liczy sobie około 2500 lat .
Powstała jako kolonia grecka o nazwie Kerkinityda .
Następnie , kolejno we władzy Rzymian i Bizancjum .
Po zdobyciu przez Turków w 1471 roku otrzymała nazwę Gözleve ( `` Gezlev '' ) .
Po 1485 część Chanatu Krymskiego .
Po przyłączeniu Krymu do Rosji w 1783 r. miasto otrzymało dzisiejszą nazwę Eupatoria .
W 1855 w trakcie wojny krymskiej w okolicach miasta doszło do bitwy , w której połączone siły turecko-francusko-brytyjskie odniosły zwycięstwo nad wojskami rosyjskimi .
Od 1954 roku w Ukraińskiej SRR , od 1991 roku – w niepodległej Ukrainie na terenie Autonomicznej Republiki Krym , w której językiem autonomii jest język rosyjski .
W mieście dnia 24 grudnia 2008 roku doszło do wybuchu gazu w którym zginęło 27 osób , a 21 zostało rannych .
