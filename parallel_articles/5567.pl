Jezus Chrystus
Jezus Chrystus ( ur .
ok. 8–4 p.n.e .
w Betlejem , zm .
ok. 30 lub 33 n.e .
w Jerozolimie ) – cieśla i kaznodzieja z rzymskiej prowincji Judea , ogłoszony przez swoich żydowskich uczniów mesjaszem , centralna postać chrześcijaństwa .
W wierze Kościoła , wyrażonej w pierwszych credo , uznawany jest za kyriosa , który zmartwychwstał ( por .
Rz 10,9 ) , zbawiciela , powszechnego władcę świata i Królestwa niebieskiego , współistotnego Bogu Ojcu syna Boga i człowieka .
Jego narodziny przyjęto za punkt zerowy naszej ery , od którego podaje się liczbę lat .
Według Nowego Testamentu ( części Biblii ) urodził się w żydowskiej rodzinie jako syn Marii poślubionej Józefowi .
Jako dorosły przyjął chrzest z rąk Jana Chrzciciela .
Następnie nauczał ( uznając przykazanie miłości za najważniejsze ) i gromadził wokół siebie uczniów ( najbliższych nazwano apostołami ) , dokonując w tym czasie cudów .
Po przyjeździe do Jerozolimy został powitany z entuzjazmem przez mieszkańców .
To , co twierdził , zwłaszcza nazywając się Mesjaszem , spowodowało wrogość faryzeuszy i saduceuszy .
Jezus przepowiedział swój los podczas Ostatniej Wieczerzy , kiedy , zgodnie z doktryną katolicką , ustanowił sakrament Eucharystii .
Zdradzony tej samej nocy przez jednego z uczniów , Judasza , został pojmany i postawiony przed religijnym sądem żydowskim , który uznał go za winnego bluźnierstwa .
Wydany został w ręce rzymskiego namiestnika Poncjusza Piłata , który pod naciskiem tłumu skazał go na śmierć przez ukrzyżowanie .
Jezus umarł na krzyżu .
Chrześcijanie wierzą , że trzeciego dnia po złożeniu jego zwłok w kamiennym grobie zmartwychwstał , a następnie przez 40 dni spotykał się ze swoimi uczniami , tłumacząc sens swojej męczeńskiej śmierci i zmartwychwstania , po czym wstąpił do nieba .
Wydarzenia opisane przez Ewangelie budzą spory naukowców , zaś inne religie postrzegają postać Jezusa w różny sposób , nie przyznając mu tak wielkiego znaczenia jak chrześcijaństwo .
Etymologia i znaczenie imienia .
Polska forma pierwszego członu imienia – Jezus – pochodzi od , które z kolei pochodzi od greckiego .
Grecka forma jest zhellenizowaną formą imienia hebrajskiego יֵשׁוּעַ i aramejskiego ܝܫܘܥ , co w obu przypadkach można odczytać jako `` Yēšuaʿ '' ( `` Joszua '' ) , które z kolei jest skróconą formą hebrajskiego יְהוֹשֻׁעַ lub יהושוע יהושע ) יְהוֹשׁוּעַ ) `` Yehōšuaʿ '' ( `` Jehoszua '' ) lub w języku polskim `` Jeszua '' ( alternatywne formy : `` Joszua '' , `` Joszue '' ) .
W języku hebrajskim imię יְהוֹשֻׁעַ ( `` Yehōšuaʿ '' ) znaczy – `` Jahwe jest zbawieniem '' .
Imię `` Joszua '' występuje także w Starym Testamencie – nosił je następca Mojżesza , Jozue ( zob .
Joz 1,1 ) .
Po hebrajsku ich imiona brzmią identycznie , lecz w tradycji polskiej jest ono inne ze względu na szacunek i wyjątkowość .
Imię Jezus nosił również autor księgi Mądrości Syracha i z tego samego powodu nazywa się ją od imienia ojca autora .
We współczesnym języku hebrajskim skrócona wersja imienia ישו ) יְשׁוּ ) ; `` Yěšû '' ( `` Jeszu '' ) jest używana jako imię Jezusa Chrystusa .
Drugi człon – Chrystus – to nie imię własne , lecz zaszczytny tytuł , wskazujący na doniosłą rolę Jezusa z Nazaretu .
