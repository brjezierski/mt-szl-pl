Jeziorko ( powiat wieruszowski )
Jeziorko – wieś w Polsce , położona w województwie łódzkim , powiecie wieruszowskim w gminie Łubnice .
W latach 1975–1998 Jeziorko należało administracyjnie do województwa kaliskiego .
Przed 2023 r. Jeziorko było częścią wsi Ludwinów .
Historia .
Jak podaje Słownik geograficzny Królestwa Polskiego w 1880 roku istniała tu osada oraz folwark , w którym znajdował się tartak i młyn zajmujący się przemiałem kości .
Całość urządzeń napędzała wtedy maszyna parowa o sile 14 KM .
Jeziorko , jako folwark połączone było z niedaleką Brzozówką .
Folwark Jeziorko zamieszkiwało wtedy około 50 osób rozlokowanych w jednym murowanym i 4 drewnianych domach .
Całkowitą wielkość folwarku Brzozówka-Jeziorko określano na 451 mórg ziemi .
Demografia .
Według danych z USC na 15 października 2019 r. zameldowanych jest tu 85 osób rozlokowanych w 29 domach mieszkalnych .
