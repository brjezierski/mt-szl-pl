Jeżów ( województwo małopolskie )
Jeżów – część wsi Wilczyska w Polsce , położona w województwie małopolskim , w powiecie gorlickim , w gminie Bobowa .
W latach 1975–1998 Jeżów należał administracyjnie do województwa nowosądeckiego .
Położenie .
Jeżów jest położony w dolinie Białej Dunajcowej , ok. 3 km na południe od Bobowej .
Zabudowania leżą na wysokości 270-300 m n.p.m .
na prawym brzegu rzeki , na północny zachód od centrum Wilczysk , przy linii kolejowej Tarnów – Stróże oraz drodze nr 981 Zborowice – Krynica-Zdrój .
Tereny Jeżowa znajdują się w obrębie Pogórza Ciężkowickiego .
Historia .
Jako wieś wzmiankowany w 1384 r. Był własnością znanego rodu szlacheckiego Jeżowskich herbu Strzemię .
Już podobno za czasów Kazimierza Wielkiego na lewym brzegu Białej wznosił się warowny zameczek , po którym pozostała nazwa wzgórza `` Zamczysko '' .
O zamku i folwarku Jeżowskich są wzmianki z 1469 r .
