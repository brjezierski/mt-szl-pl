Jimmie Johnson
Jimmie Kenneth Johnson ( ur .
17 września 1975 w El Cajon w Kalifornii ) – amerykański kierowca wyścigowy startujący w wyścigach NASCAR w serii Monster Energy NASCAR Cup Series .
Razem z Richardem Pettym i Dalem Earnhardtem jest rekordzistą wszech czasów pod względem tytułów mistrza serii , które zdobywał siedmiokrotnie .
Obecnie startuje Chevroletem Camaro ZL1 zespołu Hendrick Motorsports z numerem 48 .
Kariera .
2001–2005 : Początki w Cup Series .
W głównej serii NASCAR zadebiutował w 2001 roku na torze w Charlotte .
W kwalifikacjach zajął piętnastą pozycję , ale musiał wycofać się z wyścigu po wypadku na 192. okrążeniu .
