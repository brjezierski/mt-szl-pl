Jinja
– miejsce kultu w japońskiej religii shintō .
Odróżnia się także inne typy i rodzaje tych budowli o nazwach : `` jingū '' , `` miya '' , `` taisha '' , `` mori '' , `` hokora '' .
Sferę sacrum od profanum rozdziela brama torii .
Niektóre chramy są duże , lecz spotyka się też jedynie wydzielone place , drzewa lub małe ołtarze .
Tradycyjnie chramy shintō buduje się z drewna , bez użycia gwoździ .
