Jiří Dienstbier ( ur .
1969 )
Jiří Dienstbier ( ur .
27 maja 1969 w Waszyngtonie ) – czeski polityk i prawnik , adwokat , senator , wiceprzewodniczący Czeskiej Partii Socjaldemokratycznej .
Kandydat w wyborach prezydenckich w Czechach w 2013 , minister ds .
praw człowieka i równouprawnienia od 2013 do 2016 .
Syn Jiříego Dienstbiera .
Życiorys .
Urodził się w Stanach Zjednoczonych , gdzie jego ojciec Jiří Dienstbier , późniejszy dysydent i polityk , pracował jako dziennikarz .
Jego matka Zuzana Dienstbierová należała do sygnatariuszy Karty 77 .
Z powodu opozycyjnej działalności rodziców miał ograniczony dostęp do wykształcenia , ostatecznie pod koniec lat 80. mógł podjąć studia na wydziale mechanicznym Politechniki Czeskiej w Pradze .
W czasie studiów zaangażował się w niezależny ruch studencki .
W 1989 był współzałożycielem opozycyjnego stowarzyszenia studenckiego STUHA .
Był członkiem studenckiej komisji strajkowej i członkiem grupy , która zorganizowała masową demonstrację z 17 listopada 1989 w ramach aksamitnej rewolucji .
Reprezentował środowiska studenckie w Forum Obywatelskim .
W 1990 z ramienia tej organizacji uzyskał mandat posła do jednej z izb Zgromadzenia Federalnego Czeskiej i Słowackiej Republiki Federalnej , który wykonywał do 1992 .
Również w 1990 przeszedł również na studia prawnicze na Uniwersytecie Karola w Pradze .
Ukończył prawo w 1997 , uzyskując uprawnienia adwokata .
Podjął praktykę zawodową w ramach kancelarii prawniczej założonej przez byłego premiera Mariána Čalfę , później został jednym z jej partnerów .
Powoływany w skład różnych rad nadzorczych m.in .
Bohemians 1905 .
Od 1991 był członkiem Ruchu Obywatelskiego , do którego dołączył wraz z ojcem .
W latach 1994–1998 i ponownie od 2006 do 2010 był radnym dzielnicy Praga 2 .
W 1997 dołączył do Czeskiej Partii Socjaldemokratycznej .
Od 2011 do 2013 pełnił funkcję wiceprzewodniczącego ČSSD .
W 2010 bezskutecznie kandydował do Izby Poselskiej .
W wyborach samorządowych w 2010 był liderem listy wyborczej socjaldemokratów w Pradze , uzyskując mandat radnego miasta .
Wystąpił następnie przeciwko ponownemu zawiązaniu koalicji ČSSD z Obywatelską Partią Demokratyczną , zarzucając tej ostatniej liczne nadużycia .
W 2011 zwyciężył w drugiej turze przedterminowych wyborów do Senatu w okręgu Kladno , które rozpisano po śmierci jego ojca .
Poza socjaldemokratami poparła do także Partia Zielonych .
Już po wygranej , od 25 do 26 marca 2011 , formalnie wykonywał mandat deputowanego do Izby Poselskiej , zastępując Martina Pecinę .
Następnie przeszedł do wykonywania mandatu senatora .
19 maja 2012 Jiří Dienstbier uzyskał oficjalną rekomendację Czeskiej Partii Socjaldemokratycznej jako kandydat na prezydenta w pierwszych bezpośrednich wyborach na ten urząd .
W pierwszej turze głosowania Jiří Dienstbier otrzymał 16,12 % głosów .
Zajął 4. miejsce wśród 9 kandydatów .
W 2014 zapewnił sobie natomiast senacką reelekcję , ponownie wygrywając w drugiej turze głosowania .
29 stycznia 2014 dołączył do koalicyjnego rządu Bohuslava Sobotki jako minister ds .
praw człowieka i równouprawnienia oraz przewodniczący Rady Legislacyjnej .
Zakończył urzędowanie 30 listopada 2016 .
W 2020 utracił miejsce w Senacie , przegrywając w pierwszej turze głosowania .
Życie prywatne .
Jest stanu wolnego , ma syna Jirego .
