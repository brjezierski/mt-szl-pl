Joachim Cureus
Joachim Cureus ( również `` Curäus '' , `` Scheer '' ) ( ur .
23 października 1532 w Kożuchowie , zm .
21 stycznia 1573 w Głogowie ) – niemiecki pisarz-teolog , historyk i lekarz .
Życiorys .
Urodzony jako syn sukiennika i sędziego miejskiego Georga Scheera i jego żony Magarethy .
Od 1539 uczęszczał do szkoły w Kożuchowie , zaś od 1548 do szkoły w Złotoryi .
Przygotowany do dalszej nauki przez Valentina Friedlanda zapisał się w dniu 8 marca 1550 na uniwersytet w Wittenberdze .
Podczas studiów był m.in .
uczniem Filipa Melanchtona , w dniu 31 lipca 1554 uzyskał u Caspara Peucera stopień akademicki magistra .
Po powrocie z Wittenbergii został przez pewien czas nauczycielem szkoły miejskiej w Kożuchowie .
Następnie podjął studia medyczne na uniwersytetach w Padwie i w Bolonii , gdzie w dniu 10 września 1558 roku promował na doktora medycyny .
W 1559 roku osiadł w Głogowie , przejmując obowiązki syndyka miejskiego .
Podczas pobytu w Głogowie ( do 1564 ) przyczynił się wydatnie w procesach związanych z wprowadzeniem reformacji .
W tym okresie proponowano mu profesurę na uniwersytecie w Wittenberdze , którą jednak podobnie jak inne ( m.in .
ze Szczecina , Wrocławia ) odrzucił .
Dopiero w 1572 roku uległ propozycji księcia Jerzego Legnickiego i udał się do Brzegu .
Zmarł podczas naukowego wywodu na udar mózgu .
