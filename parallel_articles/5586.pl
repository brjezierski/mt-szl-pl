Joanna Chmielewska
Joanna Chmielewska , właściwie Irena Barbara Kuhn z domu Becker ( ur .
2 kwietnia 1932 w Warszawie , zm .
7 października 2013 tamże ) – polska architektka , autorka popularnych powieści sensacyjnych , kryminalnych , komedii obyczajowych , a także książek dla dzieci i młodzieży .
Jej książki zostały przetłumaczone na co najmniej osiem języków .
Życiorys .
Najwcześniejsze dzieciństwo pisarka spędziła w Grójcu i Warszawie .
Jako dziecko wiele czytała i w bardzo młodym wieku marzyła o napisaniu powieści .
Najpierw uczyła się w szkole powszechnej .
W 1943 roku została oddana do internatu u Sióstr Zmartwychwstanek na warszawskim Żoliborzu .
Następnie uczęszczała do gimnazjum , a później wraz rodzicami wyprowadziła się na jakiś czas do Bytomia , skąd powróciła do Grójca .
Okazało się , że jest w jednej klasie ze swoją dawną koleżanką , Janką ( późniejszą absolwentką historii na Uniwersytecie Warszawskim ) , z którą teraz mogła się zaprzyjaźnić .
Osoba ta stała się pierwowzorem postaci w kilku powieściach Chmielewskiej , podobnie jak członkowie rodziny pisarki .
Również jedna z nauczycielek została opisana w jednym z utworów – `` Zwyczajnym życiu '' .
Rodzina Chmielewskiej pragnęła , żeby została lekarką , jednak w liceum młodą Chmielewską zaczęła interesować architektura i ten kierunek studiów wybrała .
W czasach szkoły średniej poznała swojego przyszłego męża , Stanisława .
Wcześnie zaszła w ciążę i wzięła ślub .
Studia zaczęła jako mężatka , a wkrótce ( w 1951 roku ) urodziła pierwszego syna .
Ukończyła studia na Wydziale Architektury Politechniki Warszawskiej , uzyskując tytuł inżyniera architekta .
Pracowała m.in .
w Samodzielnej Pracowni Architektoniczno-Budowlanej „ Blok ” , w Energoprojekcie , w Biurze Projektów „ Stolica ” .
Brała udział w budowie Domu Chłopa .
Tematyka związana z pracą z tego okresu pojawiała się później w jej powieściach .
Debiutowała w 1958 na łamach czasopisma „ Kultura i Życie ” jako prozaik .
Przez pewien czas pisała w „ Kulturze i Sztuce ” na tematy związane z architekturą wnętrz .
W 1964 miał miejsce debiut książkowy – wydała sensacyjną powieść `` Klin '' .
Od 1970 zajmowała się wyłącznie twórczością literacką .
Łączny nakład jej powieści w Polsce przekroczył 6 mln egzemplarzy , a w Rosji – gdzie uważana była za najpoczytniejszą pisarkę zagraniczną – 8 mln .
Jej książki tłumaczono na języki obce 107 razy .
3 maja 2004 została odznaczona Krzyżem Oficerskim Orderu Odrodzenia Polski .
Życie prywatne .
Miała dwóch synów : Jerzego ( ur .
1951 ) i Stanisława Roberta ( ur .
1956 , używa głównie imienia Robert ) .
Zmarła 7 października 2013 w Warszawie .
Została pochowana 14 października 2013 na warszawskich Powązkach ( kw .
H rząd 5 grób 20 ) .
Ekranizacje .
Powstało kilka ekranizacji filmowych , opartych na jej książkach .
