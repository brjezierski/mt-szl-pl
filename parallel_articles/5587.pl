Młodość i wykształcenie .
Urodził się 20 listopada 1942 w szpitalu St. Mary ’ s Hospital w Scranton , w hrabstwie Lackawanna , w stanie Pensylwania jako Joseph Robinette Biden Jr. Jest synem Josepha Robinette ’ a Bidena Sr. i Catherine Eugenii Biden z domu Finnegan .
Wychowywał się razem z trójką rodzeństwa – dwoma braćmi ( Francisem i Jamesem ) i z jedną siostrą ( Valerie ) .
Matka Bidena była pochodzenia irlandzkiego , zaś rodzice ojca - irlandzkiego , angielskiego i francuskiego .
Uczył się w stanie Delaware , najpierw w szkole podstawowej Saint Helena ’ s Elementary School w Wilmington , a potem w prywatnej katolickiej szkole średniej Archmere Academy w Claymont .
W 1961 rozpoczął studia na University of Delaware na dwóch kierunkach – politologia i historia .
W latach 1961–1963 odbywał służbę wojskową w ramach , jednak po pierwszym roku był nisko oceniany .
W 1965 ukończył studia , uzyskując licencjat ( bachelor ’ s degree ) .
W 1968 skończył prawo na Syracuse University College of Law , zdobywając tytuł Juris Doctor .
W Archmere Academy grał w high school football ( występował na pozycji halfback i wide receiver ) oraz baseball .
Ukończył tę szkołę w 1961 roku .
Do 20. roku życia zmagał się z jąkaniem .
Dzięki recytacji poezji przed lustrem udało mu się zredukować nasilenie tego zaburzenia mowy .
Od 1969 jest członkiem izby adwokackiej Delaware State Bar Association .
W latach 1968–1970 pracował jako adwokat w Wilmington .
W latach 1970–1972 należał do Rady Hrabstwa New Castle , reprezentując 4. okręg wyborczy hrabstwa i Partię Demokratyczną .
Senator .
Wybór .
W 1972 wystartował w wyborach do Senatu w stanie Delaware , rzucając wyzwanie wieloletniemu senatorowi J. Calebowi Boggsowi z Partii Republikańskiej .
Swoją kampanią wyborczą chciał dotrzeć do osób niezainteresowanych polityką i przeciwników udziału Stanów Zjednoczonych w wojnie wietnamskiej .
Biden zwyciężył i został wybrany na pierwszą kadencję zdobywając 55,41 % wszystkich głosów .
Miał wówczas 29 lat , co oznacza , że został piątym pod względem najniższego wieku senatorem w historii Stanów Zjednoczonych .
Sześciokrotnie z powodzeniem ubiegał się o reelekcję i jest najdłużej urzędującym senatorem z Delaware w historii .
Menedżerką kampanii Joego Bidena była jego siostra – Valerie Biden Owens , odpowiedzialnym za sprawy finansowe był Jimmy Biden , natomiast funkcję koordynatora wolontariatu pełnił Francis Biden .
Działalność .
Był członkiem Senackiej Komisji Spraw Zagranicznych i Senackiej Komisji ds .
Sądownictwa .
Z tego okresu wywodzi się jego długoletnia przyjaźń z późniejszym senatorem Partii Republikańskiej , Johnem McCainem , wówczas oficerem łącznikowym marynarki przy Senacie .
Od 1987 do 1995 był przewodniczącym Senackiej Komisji ds .
Sądownictwa .
W latach 2001–2003 i 2007–2009 przewodniczył Senackiej Komisji Spraw Zagranicznych .
W 1987 wyróżnił się w czasie przesłuchania Roberta Borka , kandydata prezydenta Ronalda Reagana do Sądu Najwyższego .
W tym samym roku zdecydował się kandydować na prezydenta .
