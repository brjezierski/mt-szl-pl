Joe Biden
Joe Biden , właśc .
Joseph Robinette Biden Jr. , wym .
[ ] ( ur .
20 listopada 1942 w Scranton ) – amerykański polityk , 46. prezydent Stanów Zjednoczonych , w latach 1973–2009 senator ze stanu Delaware , w latach 2009–2017 wiceprezydent Stanów Zjednoczonych .
Edukował się w stanie Delaware .
Po zakończeniu studiów prawniczych pracował jako adwokat .
Przez całą karierę polityczną był związany z Partią Demokratyczną .
W latach 1970–1972 należał do Rady Hrabstwa New Castle .
W 1973 został wybrany do Senatu Stanów Zjednoczonych i uzyskał reelekcję w latach 1978 , 1984 , 1990 , 1996 , 2002 i 2008 .
Był piątym pod względem najniższego wieku senatorem w historii Stanów Zjednoczonych i najdłużej urzędującym senatorem z Delaware .
W tym czasie przewodził Senackiej Komisji Relacji Zagranicznych i Senackiej Komisji ds .
Sądownictwa .
Dwukrotnie bezskutecznie kandydował w wyborach prezydenckich , rezygnując w trakcie prawyborów : w 1988 i w 2008 .
W 2008 został wybrany na wiceprezydenta Stanów Zjednoczonych u boku prezydenta Baracka Obamy i uzyskał reelekcję w 2012 .
Pełnił ten urząd od 2009 do 2017 .
Po zakończeniu wiceprezydentury założył fundację charytatywną Biden Cancer Initiative , której działalność jest związana ze zwalczaniem chorób nowotworowych .
W 2019 ogłosił swoją kandydaturę w prawyborach prezydenckich przed wyborami w 2020 , z kolei w sierpniu 2020 roku oficjalnie został kandydatem Partii Demokratycznej na urząd prezydenta Stanów Zjednoczonych .
W południe 20 stycznia 2021 roku został zaprzysiężony jako 46. prezydent Stanów Zjednoczonych .
Razem z Kamalą Harris został uhonorowany nagrodą Człowiek Roku 2020 magazynu „ Time ” .
