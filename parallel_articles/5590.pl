Johan Cruijff
Johan Cruĳff , właśc .
Hendrik Johannes Cruĳff ( ur .
25 kwietnia 1947 w Amsterdamie , zm .
24 marca 2016 w Barcelonie ) – holenderski piłkarz grający na pozycji ofensywnego pomocnika , reprezentant kraju , trener .
Jeden z najlepszych zawodników w historii piłki nożnej .
Wychowanek Ajaxu , zawodnik hiszpańskiej FC Barcelony , amerykańskiego Los Angeles Aztecs , Washington Diplomats , hiszpańskiego Levante oraz Feyenoordu , trzykrotny zdobywca Złotej Piłki ( 1971 , 1973 , 1974 ) .
Z reprezentacją Holandii zdobył wicemistrzostwo świata 1974 oraz brązowy medal mistrzostw Europy 1976 .
Jako trener prowadził Ajax oraz FC Barcelonę , która pod wodzą Cruijffa nazywana była `` Dream Teamem '' oraz reprezentację Katalonii .
Poza Holandią jego nazwisko zapisywane jest często jako Cruyff ( zobacz sekcję pisownia nazwiska ) .
Wczesne życie .
Hendrik Johannes Cruĳff urodził się 25 kwietnia 1947 roku o godzinie 13 na przedmieściach Amsterdamu , przy ulicy Weidestraat , pół kilometra od stadionu De Meer , na którym grał stołeczny Ajax Amsterdam .
Dorastał w mieszczańskiej rodzinie , ojciec Hermanus Cornelis Cruĳff był właścicielem sklepu spożywczego , zaś matka – Petronella Bernarda Draaĳer pomagała w prowadzeniu rodzinnego interesu .
Od najmłodszych lat Jopie , jak zwykła wołać na niego matka , spędzał całe dnie na grze w uliczną piłkę nożną i robił to na tyle intensywnie , że tygodniowo zużywał jedną parę butów .
W wieku 10 lat został wyłowiony przez skautów Ajaksu spośród dużej grupy młodych piłkarzy .
Gdy miał 12 lat , 8 lipca 1959 jego ojciec zmarł na atak serca .
Chłopiec mocno przeżył śmierć ojca i twierdził , że wciąż czuje jego obecność .
Rodzina była zmuszona sprzedać sklep i poszukać nowych źródeł dochodu – matka Johana podjęła pracę w charakterze sprzątaczki w Ajaksie .
Dwa lata później człowiek odpowiadający za utrzymanie w murawy amsterdamskiego stadionu przyjął młodego Cruĳffa do pomocy .
Jego zadaniem było sypanie piasku na boisku po ulewnym deszczu oraz zatykanie chorągiewek w jego rogach .
W wolnych chwilach cały czas grał w piłkę , głównie ze starszymi od siebie zawodnikami .
