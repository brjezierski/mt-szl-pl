Johann Adam Steinmetz
Johann Adam Steinmetz ( ur .
24 września 1684 w Księginicach Wielkich , zm .
10 lipca 1762 ) – śląski teolog luterański , pedagog i pietysta .
Dzieciństwo i studia .
Steinmetz urodził się w księstwie brzeskim .
Jego ojciec był pastorem .
Zmarł , kiedy syn miał dwanaście lat .
Matka-wdowa Anna Barbara przeniosła się wraz z synem do Brzegu , gdzie Johann Adam uczęszczał do gimnazjum .
W wieku 20 lat rozpoczął studia na uniwersytecie w Lipsku .
Podczas studiów przeżył osobiste nawrócenie .
Jego ulubioną książką stały się rozważania Arndta „ Vom wahren Christenthum ” .
Steinmetz miał zamiar pozostać we wszechnicy jako nauczyciel – swoich planów jednak nie zrealizował .
Okres ciepłowodzki .
Zapotrzebowanie na duchownych w księstwie brzeskim oraz konieczność materialnego zabezpieczenia matki sprawiły , że Steinmetz nie pozostał na uniwersytecie .
