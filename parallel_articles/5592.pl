Jan Dzierżon ( samorządowiec )
Jan Józef Dzierżon ( ur .
7 lipca 1954 ) – polski samorządowiec i rolnik , działacz mniejszości niemieckiej , w latach 1994–1998 burmistrz Olesna , w 2003 wicewojewoda opolski .
Życiorys .
Pochodzi z okolic Olesna .
Ukończył studia na Akademii Rolniczej we Wrocławiu .
Prowadził duże rodzinne gospodarstwo rolne , w latach 90. został współwłaścicielem spółki budowlanej .
Działał na rzecz mniejszości niemieckiej , należał do Towarzystwa Społeczno-Kulturalnego Niemców na Śląsku Opolskim .
Sprawował mandat radnego powiatu oleskiego , a od 1994 do 1998 był burmistrzem Olesna .
W 2002 ponownie ubiegał się o stanowisko burmistrza ( z ramienia komitetu konkurencyjnego wobec Mniejszości Niemieckiej ) , zajmując 3 miejsce wśród 6 kandydatów .
W marcu 2003 objął funkcję wicewojewody opolskiego z rekomendacji Mniejszości Niemieckiej ( odpowiadał m.in .
za rolnictwo ) .
Odwołano go z funkcji w lipcu tego samego roku po ujawnieniu , że w 1999 prowadzony przez niego skup zboża prowadził skup interwencyjny tylko od swoich właścicieli .
W 2010 kandydował do rady powiatu oleskiego z ramienia KWW Towarzystwa Społeczno-Kulturalnego Niemców na Śląsku Opolskim , a w 2018 – do sejmiku opolskiego z ramienia Mniejszości Niemieckiej .
