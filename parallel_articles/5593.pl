Johann Wolfgang von Goethe
Johann Wolfgang von Goethe [ ] ( ur .
28 sierpnia 1749 we Frankfurcie nad Menem , zm .
22 marca 1832 w Weimarze ) – niemiecki poeta przełomu XVIII i XIX wieku i najwybitniejszy przedstawiciel literatury niemieckiej , a także jeden z najbardziej znaczących w skali światowej , dramaturg , prozaik , uczony , polityk , wolnomularz .
Był , obok Friedricha Schillera , czołowym przedstawicielem klasycyzmu weimarskiego oraz prekursorem romantyzmu .
Był również myślicielem i uczonym , znawcą wielu dziedzin wiedzy oraz sztuki .
Studia prawnicze ukończył w Lipsku i Strasburgu .
W 1775 Goethe osiadł w Weimarze , w którym mieszkał do końca życia .
Pełnił różne funkcje na dworze księcia sasko-weimarskiego , dochodząc do rangi prezydenta Izby Finansów .
W 1782 został nobilitowany .
Od 1791 do 1817 kierował teatrem oraz wszystkimi instytucjami oświatowo-kulturalnymi księstwa .
W 1786 wyjechał na dwa lata do Włoch , co wywarło duży wpływ na jego twórczość .
Najbliższa przyjaźń łączyła go z Friedrichem Schillerem .
Zmarł w Weimarze , w całkowitym osamotnieniu , po śmierci żony i jedynego syna .
Ostatnie słowa , które wypowiedział przed śmiercią , brzmiały : „ Więcej światła ” .
