John Anari
John Anari ( ur .
1980 ) – papuaski aktywista , działacz na rzecz niepodległości Papui Zachodniej .
Urodził się w wiosce Kebar , pochodzi z ludu Mpur .
Stoi na czele West Papua Liberation Organization ( WPLO ) , organizacji opowiadającej się za siłowym uzyskaniem niepodległości od Indonezji .
Jest autorem artykułu o separatyzmie papuaskim , zamieszczonego na stronach ONZ .
