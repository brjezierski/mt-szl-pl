John Baildon
John Baildon ( ur .
11 grudnia 1772 w Larbert , Szkocja , zm .
7 sierpnia 1846 w Gliwicach ) – hutnik , przemysłowiec , inżynier , uważany za ojca współczesnego hutnictwa żelaza .
Całe swoje życie zawodowe związał z Górnym Śląskiem .
Młodość .
Baildonowie byli rodziną o tradycjach hutniczych .
Ojciec Johna , William Baildon ( 1736-1820 ) , wywodził się ze starego rodu Baildonów , który od XII wieku żył w miejscowości Baildon w hrabstwie Yorkshire , niedaleko Bradford – dokładnie nie wiadomo kiedy rodzina przeprowadziła się do Szkocji .
Matką była Elisabeth Anderson .
John miał trzy siostry i trzech braci .
William Baildon był inżynierem hutnictwa w Zakładach Żelaznych Carron w Szkocji .
Młody John studiował mechanikę , hydraulikę oraz rysunek techniczny , a pod kierunkiem ojca zapoznawał się praktycznie z techniką przeróbki rud żelaza i odlewnictwem .
W wieku 17 lat poznał Fryderyka von Redena , dyrektora Wyższego Urzędu Górniczego we Wrocławiu , późniejszego pruskiego ministra zajmującego się uprzemysłowieniem Górnego Śląska .
Na Śląsku .
Wiosną 1793 przybył na zaproszenie Friedricha Wilhelma von Redena do Tarnowskich Gór .
Pomimo młodego wieku ( miał 21 lat ) powierzono mu stanowisko doradcy technicznego przy budowie Królewskiej Odlewni Żelaza w Gliwicach , gdyż jego promotorem był John Smeaton ( jeden z najwybitniejszych inżynierów tamtych czasów ) .
Główne zadanie Baildona polegało na zaprojektowaniu i nadzorowaniu budowy wielkiego pieca hutniczego opalanego koksem .
Zbudowany przez Baildona , według planów Ephraima Ludwiga Abtsa , wielki piec był pierwszym tego typu obiektem na kontynencie europejskim .
