John Forsythe
John Forsythe , właśc .
Jacob Lincoln Freund ( ur .
29 stycznia 1918 w Penns Grove , zm .
1 kwietnia 2010 w Santa Ynez ) – amerykański aktor .
Jego popularność obejmowała trzy dekady ; jako ojciec playboy Bentley Gregg w sitcomie NBC `` Ojciec kawaler '' ( `` Bachelor Father '' , 1957–62 ) , jako tajemniczy milioner Charles Townsend w popularnym serialu kryminalnym ABC `` Aniołki Charliego '' ( 1976–81 ) oraz jako bezwzględny i umiłowany patriarcha Blake Carrington w popularnej operze mydlanej ABC `` Dynastia '' ( 1981–89 ) .
8 lutego 1960 otrzymał własną gwiazdę w Alei Gwiazd w Los Angeles znajdującą się przy 6549 Hollywood Boulevard .
Życiorys .
Wczesne lata .
Urodził się w Penns Grove w New Jersey w rodzinie zasymilowanych Żydów aszkenazyjskich jako najstarszy z trójki dzieci Blanche Materson ( z domu Blohm ) i Samuela Jeremiaha Freunda , maklera papierów wartościowych .
Jego ojciec urodził się w Nowym Jorku w rodzinie polskich Żydów .
Jego matka urodziła się w Pensylwanii w rodzinie rosyjskich Żydów i niemieckich Żydów .
