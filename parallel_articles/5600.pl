John Goodman
John Goodman , właśc .
Jonathan Stephen Goodman ( ur .
20 czerwca 1952 w Affton ) − amerykański aktor i komik .
Życiorys .
Urodził się w Affton w Missouri , w hrabstwie St. Louis w rodzinie baptystów południowych pochodzenia angielskiego , niemieckiego i walijskiego .
Jego matka , Virginia Roos ( z domu Loosmore ) , była kelnerką w Jack and Phil ’ s Bar-B-Que , pracowała w sklepie , a także zajmowała się praniem , aby utrzymać rodzinę .
Jego ojciec , Leslie Francis Goodman , był pracownikiem poczty , który zmarł na atak serca , gdy John miał dwa lata .
Goodman ma młodszą siostrę Elisabeth , urodzoną sześć miesięcy po śmierci ojca , oraz starszego o 14 lat brata , Leslie .
