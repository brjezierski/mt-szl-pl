John Legend
John Legend , właściwie John Stephens ( ur .
28 grudnia 1978 w Springfield ) – amerykański piosenkarz R & amp ; B , kompozytor i pianista , zdobywca Oscara 2015 za najlepszą piosenkę ( „ Glory ” ) .
Życiorys .
Wczesne lata .
W wieku czterech lat Legend rozpoczął edukację muzyczną ; wykonywał utwory gospel na pianinie klasycznym .
Rok później dołączył do kościelnego chórku dziecięcego .
W wieku 16 lat ukończył `` Springfield North High School '' , następnie uczęszczał do uniwersytetu w Pensylwanii , w którym przewodził `` Counterparts '' , grupie śpiewającej `` a cappella '' .
Pracował w Boston Consulting Group w Bostonie , a później w Nowym Jorku .
Występował w mniejszych klubach w Nowym Jorku , Filadelfii i Waszyngtonie .
Współlokator Johna , Devon Harris , przedstawił go swojemu kuzynowi , wtedy jeszcze wschodzącej gwieździe hip-hopu , Kanye Westowi .
Od tego momentu Legend i West często pracowali razem .
