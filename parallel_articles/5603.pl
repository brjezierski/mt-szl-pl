John Lennon
John Winston Ono Lennon ( ur .
9 października 1940 w Liverpoolu , zm .
8 grudnia 1980 w Nowym Jorku ) – brytyjski muzyk , kompozytor , piosenkarz i autor tekstów .
Założyciel , muzyk i wokalista zespołu The Beatles .
Jako współlider grupy wraz z Paulem McCartneyem stworzył znaczącą większość repertuaru Beatlesów , stając się zarazem najsłynniejszym duetem pisarsko-kompozytorskim Lennon/McCartney .
W 1965 otrzymał z rąk królowej Elżbiety II Order Imperium Brytyjskiego V klasy ( MBE ) .
W 1994 został wprowadzony do Rock and Roll Hall of Fame .
W 2002 Liverpool uczcił pamięć Lennona , nazywając jego imieniem lotnisko .
W tym samym roku w plebiscycie BBC na „ 100 najważniejszych Brytyjczyków wszech czasów ” nazwisko Lennona znalazło się w pierwszej dziesiątce zestawienia .
