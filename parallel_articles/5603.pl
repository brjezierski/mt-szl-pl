XX wieku na potańcówce w Selfon Park , a ślub wzięli 3 grudnia 1938 .
Ojciec Lennona , z pochodzenia Irlandczyk , był stewardem na luksusowym statku liniowym , a matka była córką ratownika morskiego .
Z powodu przynależności do różnych warstw społecznych oraz mało stabilnej pracy Alfreda ich związek wywoływał opór rodziny Stanleyów .
Mieszkali przy Newcastle Road w podmiejskiej dzielnicy Liverpoolu , w pobliżu ulicy Penny Lane .
Gdy John miał dwa lata , jego ojciec opuścił rodzinę i wyruszył w podróż po świecie .
Matka w tym czasie wdawała się w niezobowiązujące romanse z innymi mężczyznami .
