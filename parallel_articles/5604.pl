John Major
Sir John Major ( ur .
29 marca 1943 w Carshalton w hrabstwie Surrey ) – brytyjski polityk , minister w trzecim rządzie Margaret Thatcher , lider Partii Konserwatywnej i premier Wielkiej Brytanii w latach 1990–1997 .
Na czas jego urzędowania przypada koniec zimnej wojny oraz dyskusje dotyczące stopnia integracji Wielkiej Brytanii z Unią Europejską , które doprowadziły do podziałów wewnątrz konserwatystów .
Stanowisko premiera utracił po największej od 1832 wyborczej porażce Partii Konserwatywnej .
Młodość .
Urodził się w 1943 w St Helier Hospital w Carshalton .
Jest synem Toma Pascala Huberta Majora-Balla .
Ochrzczono go pod imionami John Roy , ale w akcie urodzenia pozostało tylko pierwsze imię , jednakże aż do początku lat 80. używał imienia Roy .
