John Michael Talbot
John Michael Talbot ( ur .
8 maja 1954 w Oklahoma City , Oklahomie , USA ) – amerykański piosenkarz , kompozytor i gitarzysta , tercjarz franciszkański .
Jest popularnym piosenkarzem wykonującym utwory poświęcone głównie postaci Jezusa Chrystusa oraz tematyce chrześcijańskiej w myśl nauczania Kościoła katolickiego .
Założyciel wspólnoty `` The Brothers and Sisters of Charity '' ( Bracia i Siostry Miłosierdzia ) w Arkansas , USA .
Życiorys .
Urodził się w protestanckiej rodzinie metodystów .
Lekcje gry na gitarze rozpoczął w wieku 10 lat .
W wieku 15 lat porzucił szkołę , aby dołączyć do grupy Mason Proffit stworzonej przez jego starszego brata , w której pełnił rolę gitarzysty .
