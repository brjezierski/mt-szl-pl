Johnny B
Johnny B – singel amerykańskiej grupy rockowej The Hooters z albumu `` One Way Home '' , wydany w 1987 roku .
Utwór zajął 61 miejsce na liście Billboard Hot 100 oraz trzecie na liście Mainstream Rock Tracks .
W 1997 roku niemiecka grupa hip-hopowa Down Low nagrała cover utworu , który zajął czwarte miejsce na niemieckiej liście Top 100 Singles .
