Jonas Blue
Jonas Blue , właściwie Guy James Robin ( ur .
2 sierpnia 1989 w Londynie ) – brytyjski DJ oraz producent muzyczny .
Życiorys .
W 2015 roku Jonas Blue zyskał międzynarodowy rozgłos wydając house'ową wersję utworu „ Fast Car ” z 1988 roku , wykonywanego oryginalnie przez Tracy Chapman .
W utworze gościnnie wystąpiła brytyjska piosenkarka Dakota .
Utwór dotarł do drugiego miejsca notowania UK Singles Chart , plasując się wyżej niż oryginalnie wykonywany utwór przez Chapman , który dotarł w maju 1988 roku do 5 miejsca w tym samym notowaniu .
2 czerwca 2016 roku ukazał się nowy singel Jonasa pt .
„ Perfect Strangers ” , w którym do współpracy został zaproszony JP Cooper .
Singel powtórzył sukces poprzedniego utworu i dotarł do top 10 w większości krajów w których był notowany .
28 października został wydany kolejny singel „ By Your Side ” , w którym zaśpiewała Raye , a 5 maja 2017 roku swoją premierę miał utwór „ Mama ” z gościnnym udziałem australijskiego piosenkarza Williama Singe .
14 lipca 2017 ukazał się debiutancki album Jonasa zatytułowany `` Jonas Blue : Electronic Nature – The Mix 2017 '' .
