Jonás Gutiérrez
Jonás Manuel Gutiérrez ( ur .
5 lipca 1983 roku w Sáenz Peña ) – argentyński piłkarz występujący na pozycji skrzydłowego .
Obecnie gra w Deportivo La Coruña .
Nosi przydomek `` Spiderman '' , ponieważ zdobycie bramek dla Mallorki celebrował zakładając na twarz maskę Spidermana .
Kariera klubowa .
Jonás Gutiérrez zawodową karierę rozpoczynał w 2001 roku w CA Vélez Sarsfield .
W debiutanckim sezonie wystąpił w dziewiętnastu ligowych pojedynkach , a razem z zespołem zajął piętnaste miejsce w fazie Apertura oraz dziewiąte miejsce w fazie Clausura .
Łącznie dla Vélez Gutiérrez rozegrał 99 meczów w pierwszej lidze , a jego największym sukcesem było zwycięstwo w turnieju zamknięcia w 2005 roku .
