Twórczość .
Dzieła Eichendorffa odznaczają się optymistycznym powiązaniem z przyrodą , na przykład wiersz `` Mondnacht '' .
W noweli `` Z życia nicponia '' ( 1826 ) opowiada o prawie naiwnym młodzieńcu , który – zamiast pracować – woli raczej wędrować po kraju i ostatecznie znajduje spełnienie w początkowo nieszczęśliwej miłości .
Stworzył też znany w Niemczech utwór o charakterze patriotycznym pod tytułem `` Der letzte Held von Marienburg . ''
Spojrzenie na jego poezję z dzisiejszej perspektywy pozwala w niej dostrzec elementy tradycji i nowoczesności .
Twórczość Eichendorffa jest romantycznie religijna , miejscami z tendencjami do panteizmu , jednak osadzonego w tradycji chrześcijańskiej .
