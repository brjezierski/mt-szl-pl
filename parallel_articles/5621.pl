Joseph von Eichendorff
Joseph Karl Benedikt Freiherr von Eichendorff ( ur .
10 marca 1788 w Łubowicach , zm .
26 listopada 1857 w Nysie ) – poeta niemiecki epoki romantyzmu .
Studiował prawo .
Brał udział w wojnach napoleońskich przeciw Francji .
Następnie był urzędnikiem państwa pruskiego .
Życiorys .
Rodzice Josepha należeli do katolickiej szlachty niemieckiej .
Ojciec był pruskim oficerem , który nabył od swego teścia majątek ziemski zapewniający w miarę dostatnie bytowanie wielodzietnej rodzinie .
Jego ojciec , posiadający oprócz tego majątki ziemskie na Morawach , jednak nie potrafił w ciężkich czasach wojen napoleońskich , częściowo również w wyniku własnej niefrasobliwości , efektywnie gospodarzyć i już w roku 1801 majątek popadł w trudności finansowe .
Ostatecznie został zlicytowany i w 1822 roku po śmierci matki utracony .
Po okresie nauki domowej ( jak to było wówczas w zwyczaju ) w 1801 Józef wraz ze swym o dwa lata starszym bratem Wilhelmem został wysłany do katolickiego gimnazjum św .
Macieja we Wrocławiu , gdzie po trzech latach uzyskał maturę .
Od 1805 do 1808 roku bracia studiowali prawo w Halle i następnie w Heidelbergu , gdzie Eichendorff poznał poetów romantycznych – Clemensa Brentano i Achima von Arnima .
Po krótkiej podróży do Paryża i Wiednia bracia wrócili do majątku rodzinnego .
W wieku niespełna dwudziestu lat zamierzał wydać antologię górnośląskich baśni , jednak do druku oddał zaledwie kilkanaście opowieści , między innymi w rodzinnych stronach opowieść „ O Krasnej , pięknej róży i potworze ” .
Wkrótce okazało się , że pomimo ich wysiłków majątek nie zapewnia im środków do życia i wobec tego musieli rozejrzeć się za pracą .
Udali się do Wiednia , gdzie w 1812 złożyli egzamin państwowy referendarza , uprawniający do podjęcia służby państwowej na stanowiskach urzędniczych .
Tam też Eichendorff poznał i zaprzyjaźnił się z Fryderykiem Schlegelem i jego żoną Dorotą .
Od 1816 roku pracował w pruskiej administracji państwowej , początkowo źle opłacanej .
Mianowanie na radcę stanu ( po około 6 latach ) umożliwiło utrzymanie wielodzietnej rodziny .
W 1821 roku pracował w Gdańsku na stanowisku radcy do spraw oświaty i spraw kościoła katolickiego w Prezydium i Konsystorzu prowincji Prusy Zachodnie .
W latach 1824–1831 działał w Królewcu na stanowisku radcy stanu .
Między rokiem 1855 a 1857 mieszkał i pisał w Nysie .
Sporo czasu spędzał też w Javorniku ( wówczas Jauernig ) , na dworze Heinricha Förstera , biskupa wrocławskiego .
W Nysie zmarł i został pochowany na cmentarzu obok żony .
W 1911 roku postawiono jego pomnik na szczycie Kobylicy koło Prudnika .
Od 1956 r. Towarzystwo Literatury i Sztuki „ Wschód ” – krąg pisarzy znany jako `` Wangener Kreis '' przyznaje Nagrodę Literacką im .
