Joutseno ( stacja kolejowa )
Joutseno ( fin `` Joutsenon rautatieasema '' ) – stacja kolejowa w Joutseno , w regionie Karelia Południowa , w Finlandii .
Znajduje się na linii Kouvola – Joensuu .
Stacja została otwarta wraz z odcinkiem Lappeenranta-Imatra w październiku 1934 i została zaprojektowana przez Thure Hellströma .
Obecnie wszystkie pociągi kursujące pomiędzy Helsinkami i Joensuu , z wyjątkiem kilku kursów Pendolino zatrzymują się w Joutseno .
Kasa biletowa została zamknięta w 2003 .
Dwa wieczorne pociągi InterCity do Helsinek zatrzymują się na torze nr 2 , wszystkie pozostałe na torze nr 1 .
