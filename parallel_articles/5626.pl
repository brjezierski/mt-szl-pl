Jowisz ( mitologia )
Jowisz , Jupiter ( łac .
`` Iuppiter '' ) – w mitologii rzymskiej bóg nieba , burzy i deszczu , najwyższy władca nieba i ziemi , ojciec bogów .
Był źródłem władzy ( łac .
`` imperium '' ) , jaką dzierżyli rzymscy urzędnicy .
Rzymscy władcy i wodzowie starali się pozować na ludzkie ucieleśnienie czy odzwierciedlenie Jowisza .
Pospolicie utożsamiany jest z greckim Zeusem , z czasem przejął większość jego cech .
Atrybutami Jowisza były piorun i orzeł .
Syn Saturna .
W rzeczywistości jest to ( tożsamy z Zeusem ) praindoeuropejski bóg dziennego nieboskłonu , burzy i piorunów .
Obie nazwy , grecka i łacińska , wywodzą się z praindoeuropejskiego imienia `` Dieus ph2ter '' , co w wyniku różnych przekształceń fonetycznych dało łacińskie `` Iuppiter '' ( Iovis + pater ) .
Od dopełniacza ( `` genetivus '' ) tego imienia , brzmiącego `` Iovis '' , pochodzi polskie imię tego bóstwa , `` Jowisz '' .
Kult Jowisza , czczonego jako Jowisz Najlepszy Największy ( łac .
`` Iuppiter Optimus Maximus '' ) koncentrował się w świątyni położonej na Kapitolu w Rzymie , gdzie zasiadał on w towarzystwie Junony i Minerwy .
Czynnościami kultowymi opiekował się kapłan zwany `` flamen Dialis '' .
Jowisz i cała Trójca Kapitolińska uosabiali rzymskie państwo .
W koloniach rzymskich budowano analogiczne świątynie Trójcy Kapitolińskiej , zwane po prostu Kapitolami , rozprzestrzeniając kult poza miasto Rzym .
Na terenach starożytnego italskiego miasta Iguvium odnaleziono w 1444 roku tablice poświęcone Jowiszowi ( zobacz : Tablice iguwińskie ) .
Przydomki Jowisza : Stator – ten , który zatrzymuje chwiejące się szeregi rzymskie , Victor – dawca zwycięstwa , Versor – ten , który zmusza nieprzyjaciół do ucieczki , Ultor – karzący , niosący pomstę ( Mściciel , na podobieństwo Marsa ) .
