Urodził się roku ( według oficjalnej wersji ) w niewielkiej miejscowości Gori w Gruzji , znajdującej się wówczas pod panowaniem Imperium Rosyjskiego .
Syn Wissariona Dżugaszwilego i Katarzyny ( Jekatieriny , Ketevan ) z domu Geładze .
Popularny w gruzińskich nazwiskach przyrostek „ -szwili ” oznacza po gruzińsku „ dziecko ” , aczkolwiek nie jest znane w tym języku słowo bądź imię „ Dżuga ” .
Był dzieckiem słabym i chorowitym , trzecim w rodzinie , dwoje starszych umarło w niemowlęctwie .
Od urodzenia miał zrośnięty drugi i trzeci palec u lewej stopy .
Także jego lewa ręka odstawała od normy i była krótsza od prawej , ze względu na spowodowany w dzieciństwie uraz .
Jako ojczystym językiem posługiwał się gruzińskim , zaś po rosyjsku do końca życia mówił z wyraźnym gruzińskim akcentem .
Tematem kontrowersji pozostaje pochodzenie etniczne Stalina .
O ile zwykle uznaje się go za Gruzina , o tyle częsta jest też opinia , że był on Osetyjczykiem , co było kilkakrotnie wykorzystywane w propagandzie Gruzji , jak i Osetii Południowej , pozostających ze sobą w konflikcie .
Ojciec Józefa prowadził w Gori własny zakład szewski , gdzie zatrudniał co najmniej dziesięciu robotników , nie licząc terminatorów .
Z czasem , jego postępujący alkoholizm doprowadził do upadku zakładu .
Przez problemy z alkoholem bił chłopca i jego matkę .
Stalin starał się bronić przed agresją ojca , rzucając w niego po jednym z napadów furii nożem .
Częste libacje alkoholowe doprowadziły w końcu do wyrzucenia „ Beso ” przez matkę chłopca .
Według Montefiore Stalin w późniejszym czasie zawsze dobrze mówił o matce , którą charakteryzowała według niego „ niezłomna siła woli ” , z kolei według Radzińskiego po 1917 roku zapomniał o niej , odwiedzając ją tylko dwukrotnie .
Kobieta zajmowała się praniem bielizny dla miejscowych kupców .
Była bardzo pobożna , stąd też wysłała go w 1888 roku do szkoły cerkiewnej , aby ten uczył się na prawosławnego księdza .
W szkole zapamiętał szczególnie nauczyciela Dmitrija Chachutaszwili , który wprowadzał na lekcjach żelazną dyscyplinę , często bijąc uczniów .
W 1894 roku jako jeden z najlepszych uczniów szkoły cerkiewnej uzyskał stypendium w kwocie 5 rubli w seminarium duchownym w stolicy Gruzji , Tyflisie .
Wysyłał część pieniędzy matce , zrywając natomiast zupełnie kontakt z ojcem mającym problemy z prawem ( zginął w pijackiej bójce ) .
Stalin nie lubił seminarium .
Jak twierdził , panowały w nim „ jezuickie ” metody nauczania , opierające się na ingerencji w prywatne życie uczniów .
W trakcie studiów zainteresował go marksizm .
W 1898 roku jego nazwisko znalazło się na liście najbardziej niesfornych seminarzystów .
Przy wpisach znalazły się informacje `` Wychowanek J. Dżugaszwili czyta zakazane książki .
J. Dżugaszwili wydał nielegalną gazetkę '' .
W 1899 roku został wydalony z uczelni .
Jak sam później twierdził , wyrzucono go za promocję marksizmu .
W rzeczywistości celowo nie udał się na jeden z egzaminów , co poskutkowało automatycznym wykreśleniem go z listy studentów albo sam zrezygnował z powodu podwyższenia czesnego , którego nie był w stanie bądź po prostu nie chciał płacić .
Zachowały się po dziś dzień jego wszystkie seminaryjne świadectwa .
Wyniki w nauce , w zależności od roku studiów , miał dobre lub bardzo dobre ( niektórzy wskazują na jego niezwykłą pamięć ) .
Stalin śpiewał w chórze cerkiewnym i wyróżniał się dobrym głosem .
Początek działalności politycznej .
Po opuszczeniu uczelni wstąpił do gruzińskiej organizacji marksistowskiej Mesame dasi .
