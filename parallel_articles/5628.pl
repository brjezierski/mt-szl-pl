W latach 1919–1953 był członkiem Biura Politycznego KC RKP ( b ) , następnie WKP ( b ) i KPZR .
Formalnie pełnił obieralne , kadencyjne funkcje sekretarza generalnego KPZR i jej poprzedniczek oraz premiera ZSRR , faktycznie był dożywotnim dyktatorem posiadającym nieograniczoną władzę .
Epoka jego rządów w ZSRR jest określana jako stalinizm .
W czasie rządów Stalina w ZSRR panował masowy , państwowy terror , który pochłonął życie 20 milionów ludzi , z czego 11 milionów zmarło wskutek głodu , oraz trudną do ustalenia liczbę ofiar sowieckich deportacji , zesłań i niewolniczej pracy w łagrach ( zob .
masowe zbrodnie komunistyczne ) .
Stalin , zawierając pakt Ribbentrop-Mołotow z III Rzeszą , umożliwił jej dokonanie zbrojnej agresji na Polskę we wrześniu 1939 , przyczyniając się do wybuchu II wojny światowej .
