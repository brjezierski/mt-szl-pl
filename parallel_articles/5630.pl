João Havelange
João Havelange , właśc .
Jean-Marie Faustin Goedefroid de Havelange ( ur .
8 maja 1916 w Rio de Janeiro , zm .
16 sierpnia 2016 tamże ) – brazylijski działacz sportowy , wieloletni przewodniczący FIFA , z wykształcenia prawnik .
Życiorys .
Pochodził z rodziny pochodzenia belgijskiego .
Ukończył studia prawnicze , pracował w kilku przedsiębiorstwach transportowych .
W młodości uprawiał pływanie i waterpolo , dwukrotnie wziął udział w igrzyskach olimpijskich ( Berlin 1936 i Helsinki 1952 ) .
Od lat 50. działał w sporcie .
Był prezesem brazylijskiej federacji pływackiej , prezesem Brazylijskiej Konfederacji Sportowej , członkiem Brazylijskiego Komitetu Olimpijskiego .
W 1958 został prezesem brazylijskiej konfederacji sportu , a w 1963 członkiem Międzynarodowego Komitetu Olimpijskiego .
W czerwcu 1974 został następcą Stanleya Rousa na czele światowej federacji piłkarskiej FIFA .
Był pierwszym przewodniczącym FIFA spoza Europy .
Doprowadził do rozszerzenia puli uczestników finałów mistrzostw świata , a także liczby członków FIFA .
Działał na rzecz organizacji mistrzostw świata kobiet i mistrzostw w kategoriach młodzieżowych .
W 1998 ustąpił stanowisko szefa FIFA Szwajcarowi Seppowi Blatterowi , otrzymując tytuł honorowego przewodniczącego .
Jego imię nadano kilku stadionom w Brazylii oraz trofeum , o które walczą drużyny w mistrzostwach tego kraju .
Został odznaczony m.in .
