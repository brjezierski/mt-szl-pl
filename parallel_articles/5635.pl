Juchnajcie
Juchnajcie ( niem .
`` Juckneitschen '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie gołdapskim , w gminie Gołdap .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
Podczas akcji germanizacyjnej nazw miejscowych i fizjograficznych ( tzw .
`` chrzty hitlerowskie '' ) utrwalona historycznie nazwa niemiecka `` Juckneitschen '' została w 1935 r. zastąpiona przez administrację nazistowską sztuczną formą `` Steinhagen '' .
