W tym samym roku uległ głębokiej reorientacji , po której obecnie stanowi religię narodową Żydów .
Jest też pierwszą religią abrahamową .
Jej wyznawcy znajdują się na całym świecie , obecnie jest ich najwięcej w Stanach Zjednoczonych ( 5,6 mln ) i Izraelu ( 4,7 mln ) .
