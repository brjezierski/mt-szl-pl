Judaizm wywodzi się z wierzeń Hebrajczyków , którzy najprawdopodobniej wchodzili w skład koczowniczych plemion Habiru przybyłych do Syropalestyny zza Eufratu .
Przechowywali oni tradycję o wędrówce Abrahama , którego uznają za swojego przodka , z mezopotamskiego Ur do Kanaanu i przymierzu zawartym przez niego z Bogiem , który obiecał mu liczne potomstwo i władanie krajem Kanaan .
Znakiem tego przymierza stało się obrzezanie .
