Yugur
Yugur ( chiń .
裕固族 , Yùgù Zú ) – oficjalnie uznana mniejszość narodowa w Chińskiej Republice Ludowej .
Zamieszkuje prowincję Gansu ( głównie – ok. 90 % – Autonomiczny Powiat Yugurów Sunan ) .
Według spisu z 1990 r. liczyła ok. 13 tys .
członków .
Wywodzą się od Ujgurów , którzy w IX w. oddzielili się pod naporem Kirgizów i przenieśli na teren korytarza Gansu .
Znani jako `` Ujgurzy Hexi '' dostali się pod panowanie królestw tybetańskich , tanguckiego państwa Xixia , imperium mongolskiego i wreszcie chińskich dynastii Ming i Qing .
W wieku XVI wycofali się za Wielki Mur , aby uniknąć ataków ze strony państwa turfańskiego .
W tym czasie wykształcili już swoją odrębność kulturową , w procesie , który trwał od połowy XI w. Pod wpływem tybetańskim przyjęli buddyzm lamaistyczny , choć dawne wierzenia szamanistyczne są wciąż praktykowane ( zaginęło natomiast gnostyckie chrześcijaństwo ) .
Tradycyjnie podzieleni byli na dziewięć plemion , każde z dziedzicznymi przywódcą i jego zastępcą .
Siedem z tych plemion było dodatkowo rządzonych przez „ wielkiego wodza ” ( `` datomu '' ) .
