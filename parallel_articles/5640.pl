Ojcem pisarza był prawnik Pierre Verne , który od 1826 roku prowadził w Nantes kancelarię , a matką – Sophie-Henriette Alotte de la Fuÿe .
Młody Verne ( za namową rodziny , a głównie ojca ) studiował w Paryżu prawo , próbował zarabiać na giełdzie , ale nie rezygnował z prób literackich .
Po kilkunastu latach poszukiwań rozpoczął cykl powieści `` Niezwykłe podróże '' , które zapewniły mu poczesne miejsce w światowej literaturze .
Wydawcą utworów Verne ’ a był Pierre-Jules Hetzel ( a następnie jego syn – Louis-Jules ) publikujący `` Magasin d ’ éducation et de récréation '' ( `` Magazyn Edukacji i Rozrywki '' ) .
Znanego autora i przedsiębiorczego wydawcę łączyły kontrakty wydawnicze , kilka razy wznawiane .
Verne podróżował po świecie , jednak mniej rozlegle niż bohaterowie jego utworów .
Odwiedził 15 krajów w Europie i w basenie Morza Śródziemnego oraz odbył na statku SS „ Great Eastern ” jedną podróż przez Ocean Atlantycki do Stanów Zjednoczonych i Kanady .
Był bardzo oczytany , z literatury czerpał różnorodne wiadomości do swych książek : geograficzne , historyczne , techniczne .
Dla swych potrzeb warsztatowych stworzył i wykorzystywał duży zbiór fiszek , swoistą bazę danych z informacjami .
W wielu powieściach Verne ’ a ważną rolę pełnią bohaterowie pochodzenia francuskiego .
Kilka tytułów powstało jako efekt współpracy z innymi autorami , między innymi z André Lauriem czy Adolphem d ’ Ennery .
Książki Verne ’ a ilustrowali : Édouard Riou , Jules Férat , Léon Benett , George Roux .
