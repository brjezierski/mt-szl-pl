Julia Gillard
Julia Eileen Gillard ( ur .
29 września 1961 w Barry ) – australijska polityk , wiceprzewodnicząca Australijskiej Partii Pracy ( ALP ) i wicepremier w latach 2007–2010 .
Przewodnicząca ALP oraz premier Australii w latach 2010–2013 , była pierwszą kobietą na obu tych stanowiskach .
Życiorys .
Młodość i edukacja .
Urodziła się w 1961 w Barry w Wielkiej Brytanii .
Jej ojciec był walijskim górnikiem .
Była drugim dzieckiem , ma starszą o cztery lata siostrę Alison .
