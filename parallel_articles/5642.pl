Julian Draxler
Julian Draxler ( ur .
20 września 1993 w Gladbeck ) – niemiecki piłkarz występujący na pozycji pomocnika w portugalskim klubie SL Benfica , do którego jest wypożyczony z Paris Saint-Germain oraz w reprezentacji Niemiec .
Złoty medalista Mistrzostw Świata 2014 i Pucharu Konfederacji 2017 .
Uczestnik Mistrzostw Europy 2016 i Mistrzostw Świata 2018 .
Kariera klubowa .
Draxler pochodzi z Gladbeck , a piłkarską karierę rozpoczynał jako kilkulatek w BV Rentfort i SSV Buer .
Od 2001 trenuje w FC Schalke 04 , gdzie przechodził przez wszystkie szczeble drużyn juniorskich .
W Bundeslidze po raz pierwszy zagrał 15 stycznia w meczu z Hamburger SV , jako jeden z najmłodszych debiutantów w historii rozgrywek .
Pierwszą bramkę strzelił 1 kwietnia tego samego roku w spotkaniu z FC St. Pauli .
Z Schalke w 2011 zdobył Puchar Niemiec , strzelił pierwszą bramkę w wygranym 5:0 finale z MSV Duisburg .
W sierpniu 2015 podpisał ważny do 2020 kontrakt z VfL Wolfsburg .
W styczniu 2017 podpisał czteroipółletni kontrakt z Paris Saint-Germain .
Kariera reprezentacyjna .
Draxler ma w dorobku występy w reprezentacji Niemiec U-18 oraz U-21 .
Z kolei 26 maja 2012 zadebiutował w dorosłej reprezentacji w przegranym 3:5 towarzyskim meczu ze Szwajcarią .
W Pucharze Konfederacji 2017 w Rosji jako kapitan reprezentacji poprowadził drużynę do zwycięstwa .
