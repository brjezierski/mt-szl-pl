Julio Iglesias
Julio Iglesias , właśc .
Julio José Iglesias de la Cueva ( ur .
23 września 1943 w Madrycie ) – hiszpański piosenkarz i kompozytor .
Nakład ze sprzedaży jego wydawnictw muzycznych przekroczył 200–300 milionów na całym świecie .
On sam otrzymał łącznie około 2600 złotych i platynowych płyt .
Według Sony Music jest jednym z 10 najlepiej sprzedających się artystów muzyki w historii .
W trakcie swojej działalności muzycznej wydał ponad 77 albumów .
Śpiewa najczęściej po hiszpańsku , ale także po francusku , angielsku , włosku , niemiecku , portugalsku , nagrał też kilka utworów po katalońsku .
Jest odnotowany w `` Księdze rekordów Guinnessa '' jako artysta , który nagrał najwięcej płyt w największej liczbie języków .
Początki jego międzynarodowej sławy sięgają lat 70 .
XX wieku , kiedy zyskał popularność jako wykonawca romantycznych ballad , które w połączeniu z jego własną aparycją i wyglądem dżentelmena , dało mu niebywały sukces .
Dotychczas zagrał w przybliżeniu około 5000 koncertów .
Przez pewien czas grał w piłkę nożną na pozycji bramkarza w drużynie Realu Madryt .
