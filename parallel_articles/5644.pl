Jest najstarszym synem Dr. Julio Iglesiasa Pugi ( pochodzącego z Galicji ) oraz Marii del Rosario de la Cueva y Perignat ( pochodzącej ze szlacheckiej rodziny pochodzenia żydowskiego ) .
Jego wuj , który był ambasadorem Hiszpanii w Londynie , tytułowany był mianem markiza .
W latach 1956–1959 , będąc studentem prawa w Madrycie , przez krótki czas był bramkarzem w młodzieżowej drużynie Realu Madryt .
Wskutek wypadku samochodowego , któremu uległ 22 września 1963 , musiał zrezygnować z kariery sportowej .
Pomimo diagnoz mówiących , że nie będzie mógł chodzić , powoli zaczął wracać do zdrowia .
Aby rozwijać sprawność własnych rąk , zaczął grać na gitarze .
Kiedy wyzdrowiał , powrócił na studia .
W tym czasie przebywał w Anglii , gdzie pobierał lekcje języka angielskiego , początkowo w Ramsgate , a później w szkole językowej w Cambridge .
