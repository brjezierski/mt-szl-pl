Julius Roger
Julius Roger ( ur .
28 lutego 1819 w Niederstotzingen koło Ulm , zm .
7 stycznia 1865 w Rudach ) – niemiecki lekarz i przyrodnik , z zamiłowania etnolog , działacz społeczny .
Życiorys .
Urodził się 28 lutego 1819 roku w Niederstotzingen koło Ulm w Niemczech .
Studiował filozofię w Monachium i medycynę w Tybindze .
W 1843 roku otrzymał dyplom lekarski i rozpoczął karierę zawodową .
Na Górny Śląsk trafił w 1847 roku przyjmując posadę lekarza przybocznego księcia raciborskiego Wiktora I. Wkrótce dr Roger otrzymał tytuł Królewskiego Radcy Sanitarnego .
Oprócz leczenia rodziny książęcej swoją pracę dzielił między trzy ośrodki : Pilchowice , Rudy Raciborskie i Rybnik .
Zainicjował w Rybniku budowę nowego szpitala .
Dzieła swojego nie dokończył ze względu na nagłą śmierć w 1865 roku .
Szpital jednak powstał w latach 1868–1869 staraniem księcia raciborskiego i z pieniędzy społecznych .
Ze względu na osobę wielkiego inicjatora budowy szpital otrzymał imię „ Juliusza ” ( „ Juliuskrankenhaus ” ) , Nazwa „ św. ” pojawiła się dopiero w okresie międzywojennym .
Szpitalna kaplica jest pod wezwaniem św .
Juliusza .
Oprócz leczenia doktor Roger miał również dwie pasje : był przyrodnikiem i zbieraczem pieśni ludowych .
Od miejscowej ludności nauczył się języka polskiego i zebrał 546 pieśni , które wydał po polsku w 1863 roku pt .
`` Pieśni Ludu Polskiego w Górnym Szląsku '' .
W książce opracował pieśni śląskie , pod względem folklorystycznym porównując je z pieśniami występującymi w Polsce .
Jedną ze zidentyfikowanych wsi , w których zbierał materiały do swojej pracy były Rudy w powiecie raciborskim .
W listach do swoich niemieckich przyjaciół m.in .
Augusta von Fallersleben otwarcie dawał wyraz przekonaniu o polskości Górnoślązaków .
Utrzymywał kontakty z górnośląskimi literatami Józefem Lompą i Pawłem Stalmachem .
Upamiętnienie .
Juliusz Roger jest patronem Szkoły Podstawowej w Sośnicowicach .
Obelisk w kształcie serca na miejscu śmierci w lasach między Rachowicami a Kozłowem
W 1956 r. decyzją Ministerstwa Kultury i Sztuki nadano rudzkiemu chórowi jako jedynemu w kraju imię Juliusza Rogera .
