Jupiter ( skała )
Jupiter – skała w miejscowości Dubie w województwie małopolskim , w powiecie krakowskim , w gminie Krzeszowice .
Wznosi się u wylotu Doliny Racławki i Doliny Szklarki w grupie Skał nad Boiskiem .
Pod względem geograficznym jest to obszar Wyżyny Olkuskiej .
Skały nad Boiskiem to przygotowany w 2016 roku staraniem fundacji Wspinka nowy rejon wspinaczkowy .
Zbudowana z twardych wapieni skała Jupiter wznosi się na północno-zachodnim krańcu tej grupy skał , na otwartym terenie .
