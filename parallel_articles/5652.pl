Jurga Šeduikytė
Jurga Šeduikytė ( znana pod pseudonimem scenicznym Jurga , ur .
10 lutego 1980 w Kłajpedzie ) – litewska piosenkarka .
Na Litwie stała się znana jako finalistka bałtyckiej części konkursu muzycznego Fizz Superstar .
Później została wokalistką żeńskiego zespołu rockowego Muscat .
Zasłynęła latem 2005 piosenką pt .
`` Nebijok '' ( lit .
`` Nie bój się '' ) .
Jesienią tegoż roku wydany został debiutancki album Jurgi pt .
`` Aukso pieva '' ( lit .
`` Złota łąka '' ) .
Życiorys .
Urodzona w Kłajpedzie , w rodzinie o tradycjach muzycznych , dzieciństwo spędziła w Szydłowie i Telszach , gdzie mieszkała do drugiej klasy .
Uczyła się w telszewskim IV LO .
Następnie przeniosła się do Połągi , gdzie ukończyła liceum i szkołę muzyczną w klasie fortepianu .
Studiowała na Uniwersytecie Wileńskim , gdzie zdobyła licencjat z dziennikarstwa i tytuł magistra PR .
W 2002 roku z powodzeniem wzięła udział w Konkursie Młodych Wokalistów `` Fizz Superstar '' .
W tym samym roku , pod pseudonimem Dingau , zaczęła występy z żeńskim zespołem rockowym Muscat .
W 2004 roku grała główną rolę w musicalach `` Ugnies medžioklė su varovais '' oraz `` Tadas Blinda '' .
W latach 2004–2006 prowadziła program muzyczny `` Lietuvos dainų dešimtukas '' na stacji BTV , a wraz z Rolandasem Vilkončiusem – krajowe eliminacje do Konkursu Piosenki Eurowizji .
W 2005 roku rozpoczęła solową karierę muzyczną pod pseudonimem Jurga .
Wiosną tego samego roku piosenka pt .
`` Nebijok '' była najczęściej granym utworem w litewskim eterze , a Jurga stała się jedną z najpopularniejszych wokalistek litewskich .
W lipcu 2005 roku na Święcie Morza w Kłajpedzie dała koncert , na którym po raz pierwszy przedstawiła utwory z nadchodzącej płyty .
16 września wydała swój pierwszy album pt .
`` Aukso pieva '' , który wyprodukował Andrius Mamontovas .
Muzyka i słowa piosenki napisała sama Jurga , z wyjątkiem - napisanych przez Mamontovasa – `` Kai pamirši tu mane '' i `` Laisvė '' .
Zdobyła najwięcej litewskich nagród muzycznych za rok 2005 w różnych kategoriach .
19 kwietnia 2007 zaprezentowała i zaczęła dystrybucję długo oczekiwanego drugiego albumu pt .
`` Instrukcija '' , który w ciągu 2 tygodni zdobył złoto .
21 lipca 2007 roku Jurga zdobyła pierwszą nagrodę w Międzynarodowym Festiwalu Piosenki Bałtyckiej w Karlshamn , w Szwecji .
Zwycięską piosenką została , wykonana w języku angielskim , `` 5th Season '' z albumu `` Instrukcija '' .
1 listopada 2007 Jurga zdobyła uznanie w Monachium na MTV Europe Music Awards i z rąk Justina Timberlake ’ a odebrała nagrodę w kategorii `` Best Baltic Act '' , do której pretendowali najlepsi wykonawcy i zespoły z krajów bałtyckich .
28 sierpnia 2008 roku urodziła syna , któremu nadano imię Adas .
Ojcem dziecka jest jej przyjaciel Vidas Bareikis , wokalista Dj Suicide , z którym – 26 sierpnia roku następnego – wzięła ślub .
