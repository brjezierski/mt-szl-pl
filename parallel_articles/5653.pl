Okres młodości i szkolenie .
Urodził się we wsi Kłuszyno koło Gżacka ( przemianowanego w 1968 na Gagarin ) , w obwodzie smoleńskim .
W 1949 , po ukończeniu 15 lat , rzucił szkołę średnią ( po ukończeniu sześciu klas , „ żeby nauczyć się jakiegoś fachu ” ) w Gżacku .
W 1949 został przyjęty do szkoły zawodowej przy zakładach metalowych ( fabryka maszyn rolniczych ) w Lubiercach .
Tam szkolił się na odlewnika i zdobył zawód odlewnika-formierza .
W grudniu tego samego roku otrzymał członkostwo w Komsomole .
Wiosną 1951 rozpoczął naukę w Szkole Technicznej w Saratowie nad Wołgą .
Uprawiał sporty : zimą były to narty i łyżwy , latem lekkoatletyka i koszykówka .
Jego pasją stała się nauka latania i skoki spadochronowe .
W 1954 wstąpił do aeroklubu i rozpoczął naukę pilotażu .
W lipcu 1954 wykonał pierwszy samodzielny lot na Jaku-18 .
24 sierpnia 1955 roku ukończył kurs w saratowskim aeroklubie .
W tym samym roku Jurij , w wieku 21 lat , skończył szkołę z najwyższą oceną , został powołany do wojska i rozpoczął szkolenie u pilota testowego Jadgara Akbułatowa w Pierwszej Wojskowej Szkole Pilotów Lotniczych im .
Woroszyłowa w Czkałowie nad rzeką Ural .
8 stycznia 1956 złożył przysięgę , w lutym awansował na sierżanta .
26 marca 1957 wykonał pierwszy samodzielny lot odrzutowcem MiG-15 .
6 listopada ukończył szkołę z oceną celującą i awansował na stopień porucznika .
Pod koniec lat 50 .
XX w. był pilotem MiG-ów w 769 pułku lotnictwa myśliwskiego w Łuostari w obwodzie murmańskim , przy kole podbiegunowym w ramach Sił Powietrznych Floty Północnej .
8 marca 1960 wyjechał z bazy Nikiel do Gwiezdnego Miasteczka pod Moskwą .
W roku 1960 został jednym z dwudziestu pilotów , którzy rozpoczęli przygotowania do lotów w kosmos .
Treningi odbywały się w tajemnicy , a grupa kandydatów do pierwszego lotu w kosmos została potem zmniejszona do sześciu .
W szkoleniu wyróżniali się Jurij Gagarin i Gierman Titow .
Dopiero w czerwcu 1960 r. Gagarin wstąpił do partii KPZR .
10 kwietnia 1961 Komisja Państwowa zdecydowała , że to właśnie on będzie pierwszym człowiekiem , który poleci na orbitę okołoziemską .
O wyborze Gagarina przesądziło jego pochodzenie społeczne z prostej klasy pracującej .
Lot w kosmos .
Jurij Gagarin stał się pierwszym człowiekiem w przestrzeni kosmicznej i pierwszym sowieckim kosmonautą .
Brał udział w jednej misji kosmicznej : Wostok 1 .
12 kwietnia 1961 roku odbył w statku kosmicznym Wostok lot po orbicie satelitarnej Ziemi , dokonując jednokrotnego ( niepełnego ) jej okrążenia w ciągu 1 godziny 48 minut .
Związane z lotem momenty , które przeszły do historii według czasu GMT :
Był to pierwszy w dziejach ludzkości lot człowieka w przestrzeni kosmicznej .
Nie jest jednak jasne , jak należy ten lot sklasyfikować od strony formalnej , gdyż Gagarin nie doleciał swoim statkiem kosmicznym do Ziemi , lecz katapultował się z niego , zresztą zgodnie z planem i osiągnął powierzchnię Ziemi na spadochronie .
Fakt ten był początkowo zatajany przez ZSRR co najmniej do czasu uzyskania potwierdzenia przez FAI rekordu wysokości lotu , rekordu czasu lotu i rekordu wielkości masy wyniesionej na orbitę .
Niezależnie jednak od sposobu lądowania FAI uznała lot Gagarina .
