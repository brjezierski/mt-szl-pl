Jurij Łużkow
Jurij Michajłowicz Łużkow ( , ur .
21 września 1936 w Moskwie , zm .
10 grudnia 2019 w Monachium ) – rosyjski polityk , w latach 1992–2010 mer Moskwy .
Życiorys .
Był synem stolarza , który przeprowadził się z okolic Tweru do Moskwy .
Studiował w Instytucie Przemysłu Petrochemicznego i Gazowniczego w Moskwie .
W latach studiów pracował jako dozorca i ładowacz .
W połowie lat 70 .
XX wieku wstąpił do Komunistycznej Partii Związku Radzieckiego .
Pod koniec lat 80 .
XX wieku zrezygnował z pracy w przemyśle i został zatrudniony w urzędzie miejskim w Moskwie .
.
W 2005 roku pojawiły się kontrolowane przecieki z Kremla , że prezydent Putin zamierza usunąć Łużkowa ze stanowiska mera Moskwy .
Głosił ksenofobiczne hasła zwłaszcza w stosunku do ludzi z Kaukazu .
W 2008 roku został uznany za persona non grata na Ukrainie po tym , jak opowiedział się za przyznaniem Rosji Sewastopola .
28 września 2010 roku został odwołany ze stanowiska mera Moskwy .
Życie prywatne .
Był dwukrotnie żonaty .
W 1958 roku poślubił Marinę Baszyłową , z którą miał dwóch synów .
Po jej śmierci ( w 1988 roku ) związał się z Jeleną Baturiną , która później została uznana za najbogatszą kobietę w Rosji .
Pobrali się w 1991 roku , mają dwie córki ( ur .
1992 i 1994 ) .
Po odwołaniu ze stanowiska mera Moskwy wyjechał wraz z żoną i córkami do Wielkiej Brytanii .
Pochowany na Cmentarzu Nowodziewiczym w Moskwie .
Odznaczenia .
Został odznaczony m.in .
Orderem Zasług dla Ojczyzny I , II i III klasy , Orderem Zasług Wojskowych , Orderem Honoru , Orderem Lenina oraz Orderem Czerwonego Sztandaru Pracy .
Był też laureatem Nagrody Państwowej ZSRR .
