Justin Timberlake
Justin Randall Timberlake ( ur .
31 stycznia 1981 w Memphis ) – amerykański piosenkarz , autor tekstów , aktor , tancerz i producent muzyczny .
Wokalista popowego zespołu * NSYNC ( 1995–2002 ) .
Od 2002 artysta solowy , wydał pięć albumów studyjnych : `` Justified '' ( 2002 ) , `` FutureSex/LoveSounds '' ( 2006 ) , `` The 20/20 Experience '' ( 2013 ) , `` The 20/20 Experience – 2 of 2 '' ( 2013 ) i `` Man of the Woods '' ( 2018 ) .
Szacowany nakład ze sprzedaży jego albumów studyjnych i singli na całym świecie wynosi ponad 88 milionów egzemplarzy ( 28 milionów solo , 60 milionów z grupą 'N Sync ) .
