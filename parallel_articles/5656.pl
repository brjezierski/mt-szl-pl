Justine Dufour-Lapointe
Justine Dufour-Lapointe ( ur .
25 marca 1994 w Montrealu ) – kanadyjska narciarka dowolna specjalizująca się w jeździe po muldach , mistrzyni olimpijska i czterokrotna medalistka mistrzostw świata .
Jest młodszą siostrą Maxime i Chloé Dufour-Lapointe .
Kariera .
W 2014 roku wystartowała na igrzyskach olimpijskich w Soczi , zdobywając złoty medal i wyprzedzając swą siostrę Chloé i obrończynię tytułu , Hannah Kearney z USA .
Na mistrzostwach świata w Voss w 2013 roku zdobyła brązowy medal w jeździe po muldach .
Następnie zdobyła dwa medale podczas mistrzostw świata w Kreischbergu , zwyciężając w jeździe po muldach i zajmując drugie miejsce za Kearney w muldach podwójnych .
Zajęła również trzecie miejsce w jeździe po muldach na mistrzostwach świata w Sierra Nevada w 2017 roku , plasując się za Britteny Cox z Australii i Francuzką Perrine Laffont .
W 2018 roku zdobyła srebrny medal podczas igrzysk olimpijskich w Pjongczangu , rozdzielając Laffont i Juliję Gałyszewą z Kazachstanu .
Najlepsze wyniki w Pucharze Świata osiągnęła w sezonie 2013/2014 , kiedy to zajęła trzecie miejsce w klasyfikacji generalnej , a w klasyfikacji jazdy po muldach była druga .
Ponadto w sezonach 2011/2012 , 2012/2013 , 2014/2015 i 2015/2016 także zajmowała drugie miejsce w klasyfikacji zjazdy po muldach , a w sezonie 2016/2017 była trzecia .
W dniu 23 stycznia 2016 roku w Saint-Côme wraz z siostrami zajęła wszystkie miejsca na podium w jeździe po muldach .
Było to pierwsze w historii podium złożone z rodzeństwa w zawodach organizowanych przez FIS .
Osiągnięcia .
Puchar Świata .
Miejsca w klasyfikacji generalnej .
Od sezonu 2020/2021 klasyfikacja jazdy po muldach jest jednocześnie klasyfikacją generalną .
