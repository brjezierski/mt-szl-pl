Iuturna
Iuturna ( pol .
Juturna ) – w mitologii rzymskiej bogini źródeł i opiekunka wszystkich wód w Lacjum .
Początkowo była nimfą źródlaną o imieniu Diuturna , której oddawano cześć nad rzeką Numicius ( Numicus ) koło Lawinium .
W eposie Wergiliusza była też siostrą Turnusa , króla Rutulów , którego popierała w walce z Eneaszem ( `` Eneida '' XII , 138nn . ) .
Zakochany w niej Jowisz obdarzył ją nieśmiertelnością i władzą nad źródłami , mimo to pozostała nieczuła na jego zaloty .
Wśród mitologicznych bóstw Rzymian uważana za małżonkę Janusa i matkę Fonsa .
Pod jej opieką znajdowały się źródła , fontanny , studnie i wszelkie miejsca dostarczające wodę z głębin .
Kult jej przeniesiono do Rzymu , gdy zyskał w Italii dominację wraz z utratą znaczenia Związku Latyńskiego .
Uznawanej również za bóstwo uzdrawiające Iuturnie wzniesiono świątynię na Polu Marsowym .
Niedaleko Świątyni Westy znajdowało się na Forum Romanum jej święte źródło ( właściwie zbiornik ) , znane jako `` lacus Iuturnae '' , skąd czerpano wodę na potrzeby rytualne i lecznicze .
Doroczne święto bogini – Iuturnalia , obchodzono 11 stycznia jako dzień świąteczny pracowników wodociągów i fontann oraz straży pożarnej .
