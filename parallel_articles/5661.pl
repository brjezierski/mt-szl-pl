`` Al-Jaman '' ) , Republika Jemeńska ( arab .
, trb .
`` Al-Dżumhurijja al-Jamanijja '' ) – państwo położone w południowo-zachodniej Azji na Półwyspie Arabskim oraz na archipelagu Sokotra we wschodniej Afryce .
Jemen graniczy od północy z Arabią Saudyjską , a od wschodu z Omanem .
Od południa ma dostęp do Morza Arabskiego i Zatoki Adeńskiej , a od zachodu Morza Czerwonego .
Stolicą i jednocześnie największym miastem Jemenu jest Sana .
Innymi ważniejszymi miastami są : Aden , Ta ’ izz , Al-Hudajda oraz Al-Mukalla .
Jemen należy do Ligi Państw Arabskich .
Ustrój polityczny .
Podstawy ustroju politycznego Jemenu zostały określone w konstytucji z 16 maja 1991 z poprawkami wprowadzonymi w latach 1994 i 2001 .
