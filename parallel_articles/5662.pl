Jęczmień zwyczajny
Jęczmień zwyczajny ( `` Hordeum vulgare '' L. ) – gatunek rośliny z rodziny wiechlinowatych .
Rozpowszechniona w uprawie roślina zbożowa .
Rozmieszczenie geograficzne .
Rośnie dziko w Afryce Północnej ( Egipt , Libia ) , w Azji Zachodniej ( Afganistan , Cypr , półwysep Synaj , Iran , Irak , Izrael , Jordania , Liban , Syria , Turcja ) , na Kaukazie ( Armenia , Azerbejdżan ) , Azji Środkowej ( Kirgistan , Tadżykistan , Turkiestan , Uzbekistan ) , w Indiach , Pakistanie , Chinach , na Tajwanie .
W Europie dziko rośnie tylko na wyspie Kreta .
Jako gatunek introdukowany pojawia się w Etiopii , Korei , Australii i Nowej Zelandii .
Jest uprawiany w wielu krajach świata .
Morfologia .
Roślina jednoroczna dorastająca do wysokości 1 metra .
Kłoski boczne , w odróżnieniu od jęczmienia dwurzędowego są płodne , stąd też kłos jest wielorzędowy .
Może być 4-rzędowy , gdy środkowe kłoski są silniej przylegające do osi , lub 6-rzędowy , gdy wszystkie kłoski są jednakowo odstające .
Osadka kłosa nie kruszy się , podczas dojrzewania odpadają z niej kłoski .
Plewy o tej samej długości co plewki dolne , lub nieco krótsze .
W Polsce kwitnie w czerwcu i lipcu .
Genetyka .
17 października 2012 r. tygodnik Nature poinformował , że brytyjscy naukowcy rozszyfrowali genom jęczmienia .
Jęczmień jest diploidem o dużym haploidalnym genomie liczącym 5,1 Gpz .
