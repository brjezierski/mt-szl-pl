Jędrzejów ( Łódź )
Jędrzejów – dawna wieś , obecnie osiedle na Górnej w Łodzi , w obrębie osiedla administracyjnego Wiskitno .
Leży na południu miasta , w rejonie skrzyżowania ulic Jędrzejowskiej z Tomaszowską .
Historia .
Dawniej samodzielna miejscowość ( wieś i osada młynarska ) .
Od 1867 w gminie Wiskitno .
W okresie międzywojennym należał do powiatu łódzkiego w woj .
łódzkim .
W 1921 roku wieś Jędrzejów liczyła 367 mieszkańców .
1 września 1933 utworzono gromadę ( sołectwo ) `` Jędrzejów `` w granicach gminy Wiskitno , składającą się ze wsi Jędrzejów i Jędrzejów Młynek oraz parceli Jędrzejów .
Podczas II wojny światowej włączone do III Rzeszy .
Po wojnie Jędrzejów powrócił na krótko do powiatu łódzkiego woj .
łódzkim , lecz już 13 lutego 1946 włączono go do Łodzi .
