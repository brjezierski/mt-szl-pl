Józef Dzierzkowski
Józef Dzierzkowski ( pseudonim `` Bolesta '' ; ur .
11 stycznia 1807 w Ksawerowie w powiecie owruckim , zm .
13 stycznia 1865 ) – polski pisarz i publicysta .
Prekursor lwowskiej cyganerii literackiej , członek korespondent Towarzystwa Królewskiego Przyjaciół Nauk w Warszawie w 1829 roku .
