Józef Gara
Józef Gara ( ur .
1929 , zm .
10 lipca 2013 ) – górnik , poeta i autor piosenek w wymierającym języku wilamowskim ( `` wymysiöeryś '' ) .
W latach 2004–2006 zajmował się nauczaniem języka wilamowskiego w szkole podstawowej w Wilamowicach .
W latach 2012–2013 współpracował na zasadzie wolontariatu ze Stowarzyszeniem Wikimedia Polska .
Udostępnił swoją twórczość na licencji Creative Commons oraz brał udział w sesjach , w czasie których nagrywano wymowę słów po wilamowsku .
Zmarł 10 lipca 2013 .
Pochowany został 13 lipca 2013 w Wilamowicach .
