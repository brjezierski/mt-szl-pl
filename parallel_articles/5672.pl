Józef Latosiński
Józef Latosiński ( ur .
31 października 1861 w Czchowie , zm .
21 grudnia 1928 w Wilamowicach ) – długoletni kierownik szkoły w Wilamowicach , historyk amator , autor `` Monografii Miasteczka Wilamowic '' .
Po przybyciu do Wilamowic , w 1884 lub 1885 roku jako młody nauczyciel zafascynował się tamtejszą kulturą .
W 1908 roku napisał obszerną pracę pod tytułem `` Monografia Miasteczka Wilamowic '' , w której spisał historię miasta i zwyczaje mieszkańców oraz zajął się zagadnieniami lingwistycznymi , m.in .
kwestią pochodzenia ( języka ) Wilamowian .
Monografię wydano w 1909 roku .
W 1990 roku wydano reprint .
Józef Latosiński był również założycielem i pierwszym naczelnikiem Ochotniczej Straży Pożarnej w Wilamowicach ( 1887 ) .
