Jürgen Klopp
Jürgen Norbert Klopp ( ur .
16 czerwca 1967 w Stuttgarcie ) – niemiecki trener piłkarski , piłkarz .
Od 2015 roku trener Liverpoolu .
Kariera piłkarska .
Wychowanek klubu SV Glatten .
Później był zawodnikiem TuS Ergenzingen , w którym spędził cztery lata .
Reprezentował także barwy czwartoligowego 1 .
FC Pforzheim , Eintrachtu Frankfurt , Viktorii Sindlingen oraz Rot-Weiß Frankfurt .
W 1990 przeszedł do drugoligowego 1 .
FSV Mainz 05 .
W drugiej lidze niemieckiej zadebiutował 28 lipca 1990 w wygranym 2:1 meczu z VfB Oldenburg .
Od czasu debiutu w Mainz , Klopp był jego podstawowym graczem .
8 sierpnia 1990 w wygranym 2:1 pojedynku z Hannoverem 96 strzelił pierwszego gola w trakcie gry w 2 .
Bundeslidze .
Przez pierwsze trzy sezony występował w Mainz na pozycji napastnika .
W sezonie 1993/1994 został przesunięty na pozycję pomocnika , a od sezonu 1996/1997 grał jako obrońca .
Podstawowym zawodnikiem Mainz był do końca sezonu 1999/2000 .
Ostatnie ligowe spotkanie rozegrał tam 25 lutego 2001 , a trzy dni później został trenerem zespołu .
W barwach Mainz rozegrał w sumie 325 ligowych spotkań i zdobył 52 bramki .
Jest trzecim w historii piłkarzem Mainz pod względem ligowych występów oraz drugim pod względem liczby strzelonych bramek .
Ponadto obok Benjamina Auera jest jedynym piłkarzem w historii Mainz , który w jednym meczu strzelił cztery gole ( wygrany 5:0 mecz z Rot-Weiß Erfurt ) .
Kariera trenerska .
