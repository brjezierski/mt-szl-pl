Jēkabpils
Jēkabpils ( niem .
`` Jakobstadt '' , pol .
`` Jakubowo '' , `` Jakubów '' lub `` Jakobsztat '' ) – miasto na Łotwie , siedziba okręgu , położone nad rzeką Dźwiną , po obu jej stronach , podzielone między Łatgalię ( brzeg prawy ) i Semigalię ( brzeg lewy ) , 9. według liczby mieszkańców miasto Łotwy , 26 965 mieszkańców ( 2004 ) .
Jēkabpils leży przy drodze krajowej nr 6 łączącej Rygę z Dyneburgiem .
Miasto pełni głównie funkcje przemysłowe i handlowe .
