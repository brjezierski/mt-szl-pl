Jan I opolski
Jan I ( urodzony pomiędzy 1410 a 1413 rokiem – zmarł 5 września 1439 ) , książę opolski razem z bratem Mikołajem I w latach 1437-1439 .
Życiorys .
Jan I był drugim pod względem starszeństwa synem księcia opolskiego Bolka IV i Małgorzaty pochodzącej z leżącego we Włoszech hrabstwa Gorycji .
Po śmierci ojca w 1437 , Jan został razem z bratem Mikołajem I księciem opolskim .
6 października 1438 Jan I uznał się lennikiem Kazimierza Jagiellończyka w związku z wysunięciem jego kandydatury na tron w Czechach , jednak wobec rezygnacji króla polskiego z tytułu ostatecznie zaakceptował władzę Albrechta Habsburga ( oficjalnie nastąpiło to we Wrocławiu 3 grudnia 1438 ) .
Jan I Opolski zmarł jako kawaler 5 września 1439 .
Nie zachowały się wiadomości , gdzie został pochowany .
Nie pozostawił również potomstwa , a całość jego dziedzictwa przejął młodszy brat Mikołaj I .
