Jarnołtówek
Jarnołtówek ( daw .
`` Jarantowice '' , , ) – wieś w Polsce , położona w województwie opolskim , w powiecie nyskim , w gminie Głuchołazy .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest w Górach Opawskich .
Przepływa przez nią rzeka Złoty Potok .
Nieoficjalną częścią wsi są Bolkowice .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Do 1975 należała do powiatu prudnickiego .
Według danych na 2011 wieś była zamieszkana przez 771 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 1 km od granicy z Czechami , w północnej części Gór Opawskich .
Jarnołtówek zajmuje kilka wzgórz ( Krzyżówka , Bukowa Góra , częściowo Srebrna Kopa i Biskupia Kopa ) .
Należy do Euroregionu Pradziad .
