Józefin ( obwód brzeski )
Józefin ( ; ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie prużańskim , w sielsowiecie Wielkie Sioło , w lasach Parku Narodowego „ Puszcza Białowieska ” .
Dawniej zaścianek i majątek ziemski .
W dwudziestoleciu międzywojennym leżał w Polsce , w województwie poleskim , w powiecie prużańskim .
