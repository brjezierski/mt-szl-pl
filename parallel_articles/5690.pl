Józefosław
Józefosław ( ) – wieś w Polsce położona w województwie mazowieckim , w powiecie piaseczyńskim , w gminie Piaseczno , przy południowej granicy Lasu Kabackiego , 15 km od centrum Warszawy , w bezpośrednim sąsiedztwie miejsca katastrofy samolotu `` Tadeusz Kościuszko '' .
W 2021 roku liczba mieszkańców sięgnęła 14 806 osób .
Jest największą wsią w Polsce pod względem liczby ludności .
