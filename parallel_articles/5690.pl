Historia .
Zabór pruski .
W wyniku III rozbioru Polski w 1795 roku Piaseczno wraz z okolicami znalazło się w granicach zaboru pruskiego , na terenie tzw .
Prus Południowych ze stolicą w Warszawie .
Grunty dawnego starostwa piaseczyńskiego , należące wcześniej do króla polskiego przeszły na własność króla pruskiego a te niezagospodarowane objęto akcją kolonizacyjną .
Władze pruskie , zasiedlając tereny , kierowały się względami germanizacyjnymi , a także gospodarczymi .
