Józefów nad Wisłą
Józefów nad Wisłą ( do końca 2003 `` Józefów '' ) – miasto położone w województwie lubelskim , w powiecie opolskim , w gminie Józefów nad Wisłą , której władz jest siedzibą .
Według danych GUS z 31 grudnia 2019 r. Józefów nad Wisłą liczył 920 mieszkańców .
Józefów uzyskał lokację miejską w 1688 roku , zdegradowany w 1869 roku .
Odzyskał status miasta z dniem 1 stycznia 2018 .
Miejscowość położona jest w Małopolsce ( początkowo w ziemi sandomierskiej , a następnie w ziemi lubelskiej ) .
W latach 1975–1998 miejscowość administracyjne należała do ówczesnego województwa lubelskiego .
Miejscowość była przejściowo siedzibą gminy Rybitwy .
Przez Józefów nad Wisłą przebiega droga wojewódzka nr 824 .
Historia .
XVII wiek .
Józefów został założony w 1687 roku przez kasztelana krakowskiego Andrzeja Potockiego na gruntach wsi Kolczyn jako miasto na prawie magdeburskim .
