KWordQuiz
KWordQuiz – program , który służy głównie do nauki nowego słownictwa , będący częścią KDE Education Project , które z kolei jest częścią środowiska graficznego KDE .
Program jest dostępny na licencji GNU GPL .
KWordQuiz używa formatu kvtml .
Można go wykorzystać do nauki słownictwa praktycznie wszystkich języków świata , ponieważ w pełni wspiera Unicode .
Poza nauką słownictwa można wykorzystać go do uczenia się terminologii z wielu innych dziedzin nauki , np .
do nauki stolic państw świata .
Tryby nauki .
Program ten składa się z kilku trybów .
W jednym mamy dwie kolumny do których możemy wpisywać poszczególne słówka lub zwroty i ich tłumaczenia .
Drugi to typowy program typu flashcard , w trzeciej wyświetla się jedno słowo/zwrot oraz trzy słowa z drugiej kolumny do wyboru .
W ostatnim trybie wyświetla się słówko i należy ręcznie wpisać jego tłumaczenie .
We wszystkich trybach istnieje możliwość , czy mają się pojawiać słówka z jednego języka na drugi , odwrotnie lub losowo .
Podobne programy .
W KDE 4 KWordQuiz zastąpił KVocTrain będący częścią KDE 3 .
Istnieje także wersja dla Windows , Mac OS X oraz iPad o nazwie WordQuiz .
Oprócz tego programu w środowisku KDE możemy znaleźć program spełniający podobne funkcje o nazwie Parley .
