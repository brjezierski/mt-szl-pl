Morfologia i rozwój .
Grzyb mikroskopijny .
Kolonia `` Kabatiella zeae '' hodowana na agarze słodowym w temperaturze 24° C osiąga w ciągu 7 dni średnicę około 20 mm .
Tworzy niską grzybnię , w środku kolonii wyższą , o barwie od ciemnozielonej do czarnej , czasami w miejscach , gdzie grzybnia rośnie szybciej , ma barwę żółtą lub różową .
Strzępki bezbarwne , gładkie , cienkościenne , o szerokości 3–6 μm , w starszych kulturach stają się ciemnobrązowe , grubościenne i mogą osiągać szerokość do 9 mm .
Fialidy są niezróżnicowane , końcowe , lub boczne , w tym ostatnim przypadku często bez przegrody .
Konidia wytwarzane są zazwyczaj równocześnie w zwartych pakietach .
Zazwyczaj powstają tuż poniżej górnej części przegrody .
Po ich oderwaniu się od fialidy pozostaje długo utrzymująca się blizna lub ząbek o średnicy 1 μm .
Konidia są jednokomórkowe , gładkie , sierpowatego kształtu i mają rozmiary 18,5–33,5 ×2–3,5 μm .
Nie obserwowano konidiów wtórnych ani endokonidiów .
Na pierwszy rzut oka pod mikroskopem `` Kabatiella zeae '' przypomina grzyby z rodzajów `` Idriella '' lub `` Microdochium '' , łatwo może być jednak od nich odróżniona .
Charakteryzuje ją bowiem jednoczesne tworzenie konidiów oraz cylindryczne fialidy .
Znaczenie .
Wywołuje chorobę o nazwie drobna plamistość liści kukurydzy ( zwana też antraknozą kukurydzy ) .
