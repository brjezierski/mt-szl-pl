Kabatina juniperi
Kabatina juniperi R. Schneid .
& amp ; Arx – gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Kabatina , Dothioraceae , Dothideales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1966 r. Roswitha Schneider i Josef Adolph von Arx na liściach kilku gatunków jałowców w Niemczech i Holandii .
Charakterystyka .
`` Kabatina juniperi '' to jeden z wielu gatunków grzybów pasożytniczych porażających jałowce ( `` Juniperus '' ) .
Wywołuje u nich chorobę o nazwie zamieranie wierzchołków jałowca .
Oprócz niego głównymi sprawcami tej choroby są `` Diaporthe juniperivora '' i `` Sydowia polyspora '' .
`` Kabatina juniperi '' zimuje w porażonych i obumarłych pędach , na korze jałowców lub na ziemi .
Jej owocniki rozwijają się wiosną , a w okresie wilgotnej pogody uwalniają zarodniki dokonujące infekcji pędów .
Wnikają do rośliny przez rany , zarówno te spowodowane mechanicznie , np .
podczas przycinania krzewów , jak i przez drobne ranki spowodowane przez owady .
Przy sprzyjającej grzybowi wilgotnej pogodzie patogen rozprzestrzeni się w krzewie w ciągu kilka lat lub mniej .
Porażone końcowe części pędów jałowca na długości od 5 do 35 cm najpierw stają się matowo zielone , potem czerwone lub żółte .
U podstawy przebarwionej tkanki widoczne są drobne zmiany w kolorze popielatoszarym do srebrnych , nakrapiane małymi czarnymi owocnikami grzyba .
Brązowe , wysuszone liście w końcu opadają z drzewa pod koniec maja lub w czerwcu .
Choroba pojawia się tylko wczesną wiosną ( luty-marzec ) .
