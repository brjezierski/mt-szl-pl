Kabatina thujae
Kabatina thujae R. Schneid .
& amp ; Arx – gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Kabatina , Dothioraceae , Dothideales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1966 r. Roswitha Schneider i Josef Adolph von Arx na pędach żywotnika zachodniego ( `` Thuja occidentalis '' ) w Niemczech .
Charakterystyka .
Mikroskopijny grzyb pasożytniczy porażający pędy żywotników ( `` Thuja '' ) , cyprysów ( `` Cupressus '' ) i cypryśników ( `` Chamaecyparis '' ) .
Wywołuje u nich chorobę o nazwie zamieranie pędów żywotnika .
Najsilniej atakuje żywotnika zachodniego .
