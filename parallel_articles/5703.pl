Kabul
Kabul ( dari , paszto کابل ) – stolica i największe miasto Afganistanu , liczące ponad 4,6 mln mieszkańców ( 2021 ) , położone w prowincji ( wilajecie ) Kabul ; główny ośrodek gospodarczy , naukowy i kulturalny kraju ; w starożytności znane jako `` Ortospana '' , `` Kabura '' ( ) , `` Kophes '' lub `` Kophene '' .
Położenie .
Kabul leży na wysokości 1807 m n.p.m. , w wąskiej kotlinie górskiej , ograniczonej górami Hindukusz na północy i Spin Ghar na południu , nad rzeką Kabul .
Miasto jest połączone okrężną autostradą tzw .
Ring Road z Ghazni , Kandaharem , Heratem oraz Mazar-e Szarifem .
Historia .
Kabul ma ponad 3000 lat , wiele imperiów walczyło o to miasto z powodu jego strategicznego położenia wzdłuż szlaków handlowych południowej i centralnej Azji .
Stanowił jeden z najbardziej wysuniętych na wschód rejonów Imperium Achemenidów .
