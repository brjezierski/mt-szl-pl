Kachowka
Kachowka ( ukr .
Каховка ) – miasto na południu Ukrainy , w obwodzie chersońskim , od lutego do listopada 2022 pod okupacją rosyjską .
Liczy 35,9 tys .
mieszkańców .
Miasto leży nad Zbiornikiem Kachowskim , jest ośrodkiem przemysłu maszynowego , metalowego oraz spożywczego .
Do atrakcji turystycznych należy pomnik legendarnej taczanki , upamiętniający bitwę pod Kachowką podczas rosyjskiej wojny domowej .
Historia .
Miasto założone 17 stycznia 1791 roku jako własność pułkownika D.M .
Kulikowskiego na miejscu dawnej tureckiej fortecy Islam-Kermen .
Miasto okupowane przez wojska Federacji Rosyjskiej od początku inwazji Rosji na Ukrainę w 2022 .
