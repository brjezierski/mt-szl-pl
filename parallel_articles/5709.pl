Kaduceusz
Kaduceusz ( łac .
`` caduceus '' „ laska heroldów ” ) , kerykejon ( gr .
`` kērýkeion '' ) , oba od ( `` kēryks '' „ herold ” ) – symboliczna laska .
Symbolika .
Stanowi symbol ( znak ) pokoju i handlu .
W sztuce przedstawiana jest zwykle w formie prostego kija ( pierwotnie z drewna oliwnego lub laurowego ) z dwoma oplatającymi go wężami , które pochylają ku sobie łby ( symbolizują mądrość , równowagę sił ) .
Niekiedy bywa zakończona parą skrzydeł lub ozdobiona wieńcem , przewiązana wstążkami .
Czasem jest błędnie używana jako symbol medycyny , zwłaszcza w Stanach Zjednoczonych , z powodu mylenia jej z tradycyjnym symbolem medycyny , jakim jest laska Asklepiosa .
Pojawia się również w 7 scenie II aktu `` Wesela '' Stanisława Wyspiańskiego jako laska błazna , wręczona Dziennikarzowi przez Stańczyka .
Kaduceusz w mitologii .
W starożytnej Grecji kaduceusz był oznaką nietykalności keryksa i służyła do uśmierzania sporów .
Według mitologii greckiej i rzymskiej należała do atrybutów boskich posłańców : boga Hermesa ( Merkurego ) i bogini Iris .
Wykorzystanie w heraldyce .
Kaduceusz znajduje się na herbie Ekwadoru oraz herbie wenezuelskiego stanu Bolívar .
W polskiej heraldyce symbol ten użyty został w dawnym herbie Rzeszowa , herbie Marek oraz herbie gminy Nur .
Jest to również symbol często używany w heraldyce ukraińskiej i rosyjskiej , m.in .
w herbach Charkowa i obwodu charkowskiego , Armawiru , Ułan Ude , Nowozybkowa , Zielonodolska .
