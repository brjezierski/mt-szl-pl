Kaduna ( rzeka )
Kaduna – rzeka w środkowej Nigerii , lewy dopływ Nigru .
Jej długość wynosi 550 km .
Źródła rzeki Kaduna znajdują się w zachodniej części wyżyny Dżos .
Ważniejsze miasta nad Kaduną to Kaduna i Zungeru .
Rzeka jest żeglowna w czasie wysokich stanów od Zungeru .
W górnym biegu ma charakter rzeki okresowej .
Nazwa rzeki pochodzi od słowa `` kada '' , które w języku hausa znaczy `` krokodyl '' .
