Kaili
Kaili ( chiń .
凯里 ; pinyin : `` Kǎilǐ '' ) – miasto w południowych Chinach , w prowincji Kuejczou , siedziba prefektury autonomicznej Qiandongnan , port nad rzeką Longtou Jiang .
W 1999 roku miasto liczyło 418 501 mieszkańców .
Powierzchnia miasta wynosi 1306 km² .
Ośrodek wydobycia węgla kamiennego oraz przemysłu metalurgicznego , drzewnego i spożywczego .
Duży odsetek mieszkańców stanowią przedstawiciele mniejszości etnicznej Miao .
