Kaiserslautern
Kaiserslautern – miasto na prawach powiatu w południowo-zachodnich Niemczech , w kraju związkowym Nadrenia-Palatynat , siedziba powiatu Kaiserslautern oraz gminy związkowej Kaiserslautern-Süd .
Miasto leży przy autostradzie A6 , mniej więcej w połowie drogi między Mannheim a Saarbrücken ( ok. 70 km od obu miast ) , leży na północno-zachodnich obrzeżach Lasu Palatynackiego .
Miasto liczy 99 275 mieszkańców ( 31 grudnia 2009 ) i jest jednym z większych miast Palatynatu .
Historia .
Miasto zostało założone przez cesarza Fryderyka Barbarossę ok. 1150 .
Wcześniej , nad znajdującą się tu rzeką Lauter od IX wieku istniał tu dworek myśliwski cesarzy .
Fryderyk Barbarossa rozpoczął budowę miasta i do dziś jest mile wspominany .
Z tego powodu Kaiserslautern nosi czasem przydomek `` Die Barbarossastadt '' ( pol .
„ Miasto Barbarossy ” ) .
Prawa miejskie uzyskało w 1276 .
W 1794 Kaiserslautern wraz z zachodnią częścią Palatynatu zostało zajęte , a w 1801 formalnie zaanektowane przez Republikę Francuską .
Po kongresie wiedeńskim ( 1815 ) Palatynat znalazł się w obrębie Królestwa Bawarii .
W 1900 założono popularny klub piłkarski 1 .
FC Kaiserslautern .
