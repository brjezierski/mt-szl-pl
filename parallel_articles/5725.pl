Kaja Godek
Kaja Urszula Godek ( ur .
2 maja 1982 w Warszawie ) – polska aktywistka antyaborcyjna , konserwatystka , z wykształcenia anglistka .
Życiorys .
Pochodzi z Warszawy , ukończyła anglistykę na Uniwersytecie Warszawskim i pracowała jako asystentka prezesa w prywatnej firmie .
Rozpoznawalna stała się w 2012 , po występie w programie Tomasza Lisa w TVP2 , w którym brała udział w dyskusji na temat aborcji jako przedstawicielka poglądów Fundacji Pro Mariusza Dzierżawskiego , do której wstąpiła w 2008 .
W latach 2015–2019 była członkiem rady nadzorczej Warszawskich Zakładów Mechanicznych , będących spółką państwową ( wówczas , po 2014 roku , 85 % akcji należało do Agencji Rozwoju Przemysłu ) .
Podniesiony w mediach zarzut braku jej kompetencji do sprawowania swojej funkcji spotkał się ze zwolnieniem Godek ze stanowiska decyzją walnego zgromadzenia akcjonariuszy .
Od stycznia 2016 jest członkiem zarządu Fundacji Życie i Rodzina ( KRS ) .
