Kair
Kair ( , wymowa egipska , trl .
`` Al-Qāhirah '' , trb .
`` Al-Kahira '' ) – stolica i najludniejsze miasto Egiptu ( 606 km² , liczba ludności 19,5 mln stałych mieszkańców , zespół miejski 20,4 mln mieszkańców , tzw .
`` Wielki Kair '' ) , największe miasto Bliskiego Wschodu , a do 2013 roku – także Afryki .
Nie ma dokładnych statystyk na temat liczby ludności Kairu .
Powodują to ciągłe wędrówki mieszkańców .
Szacuje się , że codziennie przybywa tu tysiąc nowych mieszkańców .
Ponad 2 mln osób mieszka w tzw .
„ Mieście umarłych ” .
Kair nazywane jest „ Miastem tysiąca minaretów ” lub „ Matką Świata ” .
To największy ośrodek świata islamskiego .
Położone jest nad Nilem .
Nazwa .
W języku arabskim literackim Kair określa się mianem „ al-Qahira ” , które nawiązuje do arabskiej nazwy planety Mars – Qahir al-Falak , i dosłownie tłumaczy się „ zwycięska ” ( słowo „ miasto ” w języku arabskim jest rodzaju żeńskiego ) .
Nazwa Al-Qahira została zniekształcona przez Wenecjan , którzy prowadzili tu w średniowieczu swoje interesy , i w takiej formie trafiła m.in .
do języka polskiego .
W dialekcie kairskim nazwa miasta ( a także całego Egiptu ) to „ Masr ” .
Nazywane jest również „ Miastem tysiąca minaretów ” , „ Matką Świata ” , „ Wielkim Mango ” .
Geografia .
`` Wielki Kair '' leży po obu brzegach Nilu oraz na licznych wyspach na rzece , nieco na południe od miejsca , w którym Nil przechodzi w deltę .
Metropolia składa się z dwóch miejskich guberni : Kairu i Gizy tworząc jeden `` organizm '' miejski .
Miasto dzieli się na 14 dzielnic .
Najstarsza dzielnica miasta leży na wschodnim brzegu Nilu .
Dalej , miasto rozpościera się na zachód , zajmując dawne tereny rolnicze .
Zachodnie dzielnice zbudowano w XIX w. , w nawiązaniu do wzorców zachodnioeuropejskich ( m.in .
Paryża po roku 1870 ) , i dlatego więcej jest tam szerokich alei , zieleni i otwartych przestrzeni .
Natomiast stare , wschodnie dzielnice , mają zupełnie odmienny wygląd : gęsta zabudowa , małe , zatłoczone alejki , kończące się często ślepymi zaułkami .
Dlatego większość biur i budynków rządowych mieści się w zachodniej części miasta , a dzielnice wschodnie cechuje duża liczba zabytkowych meczetów .
Miasto rozszerza się także na tereny pustynne .
Obydwa brzegi Nilu spięły mosty .
Przy pomocy mostów połączyły się z lądem stałym również wyspy Gezira i Roda , na których mieści się większość obiektów rządowych .
Ze wschodnich dzielnic , mostami , łatwo jest przedostać się do przedmieść na lewym brzegu : Gizy i Imbaby .
