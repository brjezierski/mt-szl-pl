Kakapo ( rodzina )
Kakapo , kakapowate ( Strigopidae ) – rodzina ptaków z rzędu papugowych ( Psittaciformes ) .
Zasięg występowania .
Rodzina obejmuje gatunki występujące w Nowej Zelandii .
Systematyka .
Rodzina Strigopidae została nazwana w 1849 roku przez Karola Lucjana Bonaparte .
Strigopidae obejmuje najbardziej bazalne papugowe .
Do rodziny należy jeden występujący współcześnie rodzaj i gatunek :
Do rodziny należy również rodzaj `` Nelepsittacus '' , do którego zaliczają się trzy gatunki ; okres ich istnienia datowany jest na wczesny miocen ( 16–19 mln lat temu ) .
Rozmiary tych trzech gatunków wahały się między wielkością przedstawicieli `` Cyanoramphus '' a nestorami kea ( `` Nestor notabilis '' ) .
