Kalamata
Kalamata ( gr .
`` Καλαμάτα '' ) , wcześniej `` Kalame '' , jeszcze dawniej `` Kalamai ( Καλάμαι ) '' , w okresie mykeńskim `` Fares ( Φαρές ) '' – miasto w Grecji , na południowo-zachodnim Peloponezie , w administracji zdecentralizowanej Peloponez , Grecja Zachodnia i Wyspy Jońskie , w regionie Peloponez , w jednostce regionalnej Mesenia .
Siedziba gminy Kalamata .
W 2011 roku liczyło 54 100 mieszkańców .
