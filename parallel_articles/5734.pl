Koleśniki ( sielsowiet Turkowo )
Koleśniki – dawna kolonia .
Tereny , na których leżała , znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie miorskim , w sielsowiecie Turkowo .
Historia .
W czasach zaborów w powiecie dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
Pod koniec XIX wieku należała do dóbr Łąka , własność Frołowa .
W latach 1921–1945 wieś a następnie kolonia leżała w Polsce , w województwie wileńskim , w powiecie dziśnieńskim , w gminie Mikołajów .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwały tu 23 osoby , 4 były wyznania rzymskokatolickiego a 19 prawosławnego .
Jednocześnie wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Było tu 5 budynków mieszkalnych .
W 1931 w 5 domach zamieszkiwało 28 osób .
Wierni należeli do parafii prawosławnej i rzymskokatolickiej w Dziśnie .
Miejscowość podlegała pod Sąd Grodzki w Dziśnie i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Mikołajowie Dziśnieńskim .
