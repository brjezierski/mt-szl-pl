Kaliber 44
Kaliber 44 – polska grupa muzyczna wykonująca hip-hop .
Zaliczana do najważniejszych przedstawicieli gatunku w Polsce .
Powstała w 1994 roku w Katowicach z inicjatywy braci Michała „ Ś.P .
Brata Joki ” Martena i Marcina „ Abra dAba ” Martena .
Wkrótce potem skład uzupełnił Piotr „ Mag Magik I ” Łuszcz .
W 1998 roku po opuszczeniu składu przez Łuszcza formuła Kaliber 44 została rozwinięta o koncertową formację Baku Baku Skład , której członkami byli : bracia Martenowie , Maciej „ WSZ ” Gnatowski , Tomasz „ CNE ” Kleyff , Bartosz „ DJ Bart ” Jabłoński , Sebastian „ DJ Feel-X ” Filiks oraz Piotr „ Gutek ” Gutkowski .
W 2003 roku formacja zaprzestała działalności , Marcin „ Abra dAb ” Marten rozpoczął solową działalność artystyczną .
Jego starszy brat Michał podjął pracę zawodową , sporadycznie gościł także na płytach innych wykonawców .
Natomiast Sebastian „ DJ Feel-X ” Filiks pozostał aktywnym członkiem krajowej sceny muzycznej .
Nawiązał m.in .
współpracę z Kapelą Ze Wsi Warszawa .
W latach późniejszych zespół dał kilka koncertów , które nie stanowiły jednak o wznowieniu jego działalności .
Ostatecznie 2013 roku doszło do oficjalnej reaktywacji grupy .
Równolegle działalność wznowił koncertowy skład towarzyszący K44 – Baku Baku Skład .
W szczytowym okresie działalności przypadającym na lata 1996–2000 , grupa wydała trzy albumy studyjne pozytywnie oceniane przez krytyków muzycznych , jak i publiczność .
Obok takich zespołów jak Molesta Ewenement , Slums Attack , czy Nagły Atak Spawacza K44 z dorobkiem ponad 150 tys .
sprzedanych płyt należy do najpopularniejszych zespołów hip-hopowych w Polsce .
Kaliber 44 powszechnie jest zaliczany do nurtu hardcore psycho rapu , gatunku , terminu ukutego przez sam zespół .
Faktycznie jednak ów gatunek nigdy nie uległ wykształceniu .
Historia .
Grupa powstała w 1994 roku w Katowicach z inicjatywy nastoletnich raperów , braci Michała Martena znanego jako „ Ś.P .
Brat Joka ” i Marcina Martena występującego pod pseudonimem „ Lord MM Dab ” .
Wkrótce do zespołu dołączył Piotr Łuszcz występujący jako „ Mag Magik I ” .
Początkowo zespół współpracował z katowickimi raperami Rafałem „ Jajonaszem ” Łukaszczykiem i Rafałem „ Gano ” Wywiołem .
Teksty pisane były w języku angielskim , natomiast muzyka była oparta na gitarowych riffach .
Pierwsze nagrania tria ukazały się na kasecie magnetofonowej pt .
`` Usłysz nasze demo '' .
Kolejne dwa lata formacja spędziła na produkcji kolejnych nagrań i poszukiwaniach wydawcy .
