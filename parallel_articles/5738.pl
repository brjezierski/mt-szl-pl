Kalinów ( Piława Górna )
Kalinów ( niem .
`` Sadebeckhoh '' ) – dzielnica miasta Piława Górna w województwie dolnośląskim , w powiecie dzierżoniowskim .
Nazwa i położenie .
Nazwa `` Sadebeckhoh '' pochodzi od nazwiska Augusta Sadebecka ; znajduje się w północno-wschodniej części Piławy Górnej , obok dzielnicy Kośmin .
Położona jest na Wzgórzach Gumińskich .
Historia .
Sadebeckhoh został zbudowany jako osada tkacka ok. 1820 , jego fundatorem był August Sadebeck .
Miejscowość należała swym kształtem do wyjątków urbanistycznych , ponieważ rozplanowano ją z czworokątnym rynkiem w środku oraz czterema ulicami z niego wychodzącymi .
Polską nazwę Kalinów nadano miejscowości w 1947 .
W październiku 1954 Kalinów ( wraz z Kośminem , Kopanicą i Piławą Górną ) wszedł w skład nowo utworzonej gromaday Piława Górna .
1 stycznia 1956 gromadzie Piława Górna nadano status osiedla , co oznaczyło , że Kalinów stał się częścią tej skonsolidowanej jednostki , a więc w praktyce został włączony do Piławy Górnej .
18 lipca 1962 osiedle Piława Górna otrzymało status miasta .
Kalinów `` de facto '' stanowi południową część Kośmina ( obecnie ul .
Leśna ) .
