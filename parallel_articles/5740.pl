Kaliska ( Skarżyce )
Kaliska ( 401,5m ) – wzniesienie w Skarżycach w województwie śląskim .
Jest bezleśne i znajduje się wśród pól uprawnych , po północnej stronie zabudowanego obszaru Skarżyc .
Na północno-wschodnich zboczach znajdują się wapienne skały o nazwie Grań Basztek , w odległości około 360 m na południe od szczytu Kaliskiej znajduje się drugie wzniesienie z wapiennymi skałkami – Wzgórze pod Skałką .
Pod względem geograficznym jest to obszar Wyżyny Częstochowskiej .
