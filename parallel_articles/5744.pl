mieszkańców .
Położenie .
Kaliningrad leży na Półwyspie Sambijskim u ujścia Pregoły do Morza Bałtyckiego , 35 km od granicy polskiej , 70 km od granicy litewskiej i 1289 km od Moskwy .
Historycznie leży na terenie Prus Dolnych i Sambii .
