Kaliningrad
Kaliningrad , Królewiec ( ; dawn . )
– miasto należące do Rosji , położone przy ujściu Pregoły do Zalewu Wiślanego , stolica obwodu kaliningradzkiego .
Jest to ważny morski port handlowy i wojenny , połączony torem wodnym z otwartym Morzem Bałtyckim i awanportem Bałtyjsk , a także ośrodek przemysłu środków transportu , maszynowego , celulozowo-papierniczego i rybnego .
Stanowi węzeł kolejowy , obsługiwane jest przez port lotniczy w pobliskim Chrabrowie .
W mieście działają liczne szkoły wyższe ( m.in .
uniwersytet i uniwersytet techniczny ) .
