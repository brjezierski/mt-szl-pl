Kamaishi
– miasto portowe w Japonii , na wyspie Honsiu , w prefekturze Iwate .
Ma powierzchnię 440,35 km2 .
W 2020 r. mieszkało w nim 32 096 osób , w 14 723 gospodarstwach domowych ( w 2010 r. 39 578 osób , w 16 095 gospodarstwach domowych ) .
Położenie .
Miasto leży we wschodniej części prefektury nad Oceanem Spokojnym .
Graniczy z miastami : Tōno oraz Ōfunato .
Historia .
Miasto powstało 5 maja 1937 roku .
Przemysł .
W mieście rozwinął się przemysł maszynowy , metalowy oraz hutniczy .
