Kambodża
Kambodża , Królestwo Kambodży ( , trl .
`` Kâmpǔchéa '' , `` Preăhréachéanachâkr Kâmpǔchéa '' , w latach 1976–1989 Kampucza ) – państwo w południowo-wschodniej Azji , na Półwyspie Indochińskim , nad Zatoką Tajlandzką .
Graniczy od zachodu i północy z Tajlandią ( długość granicy – 803 km ) , od północy z Laosem ( 541 km ) , a od wschodu z Wietnamem ( 930 km ) .
Geografia .
Kambodża położona jest między 10°50 ' a 15°00'N oraz 102°50 ' a 107°50 ' E .
Rzeźba .
75 % powierzchni kraju zajmuje równinna , aluwialna Nizina Mekongu , wypełniająca młode zapadlisko tektoniczne osadami czwartorzędowymi .
Przez jej wschodnią część przepływa główna rzeka kraju Mekong , a w zachodniej części leży rozległe jezioro Tonle Sap ( 2,7-16 tys .
km² ) .
Na południowy wschód od równiny leży wierzchołek delty Mekongu , rozciągającej się na terytorium Wietnamu w kierunku Morza Południowochińskiego .
Centralna równina oraz delta otoczone są pasmami górskimi od południowego zachodu ( Góry Kardamonowe i Góry Słonia oraz od północy Góry Dangrek ) .
Góry Kardamonowe ( najwyższy szczyt kraju – Phnum Aôral 1813 m n.p.m . )
i Góry Słonia tworzą naturalną barierę fizycznogeograficzną ciągnącą się wzdłuż całego wybrzeża ( małe przybrzeżne wyspy , największa w zatoce Kompong Som ) .
Góry Dangrek stanowią naturalną północną granicę kraju .
Na wschód od Mekongu teren stopniowo podnosi się w kierunku Gór Annamskich .
Pomiędzy obszarami górskimi a równiną centralną rozciąga się równinny obszar przejściowy o średniej wysokości nieprzekraczającej 200 m n.p.m .
Klimat w Kambodży .
W Kambodży panuje klimat zwrotnikowy monsunowy z wyraźnym podziałem na dwie główne pory roku : suchą i deszczową .
Wiejący od połowy maja do połowy października południowo-zachodni monsun przynosi ulewne deszcze .
Od początku listopada do połowy marca słabe wiatry północno-wschodnie .
W porze tej zachmurzenie jest zmienne , wilgotność powietrza niska , a deszcze padają sporadycznie .
Średnia temperatura miesięczna waha się od 21 °C w grudniu do 30 °C w maju .
Średnia temperatura roczna na terenach nizinnych wynosi ok. 25 °C .
Temperatury są najwyższe tuż przed rozpoczęciem pory deszczowej i mogą przekraczać 38 °C .
Najwyższe opady atmosferyczne notuje się na eksponowanych w kierunku morza stokach górskich ( 5000 mm ) , a najniższe na nizinnych obszarach centralnych ( 750–1400 mm ) .
Średnia roczna suma opadów wynosi 1000–2000 mm .
Ponad 70 % przypada na okres wilgotnego letniego południowo-zachodniego monsunu .
Wody .
Sieć rzeczna Kambodży jest dobrze rozwinięta .
Prawie cały obszar kraju znajduje się w dorzeczu Mekongu .
Dział wodny I stopnia stanowią pasma Gór Kardamonowych i Gór Słonia .
Rzeki na wschodnich zboczach tych gór spływają do jeziora Tonle Sap , a na zachodnich do Zatoki Tajlandzkiej .
Osią hydrograficzną kraju jest Mekong płynący na terenie Kambodży w kierunku południowym na odcinku ok. 500 km .
Drugim ważnym elementem hydrograficznym kraju jest jezioro o powierzchni od 2600 km² w porze suchej do 10400 km² w porze deszczowej .
Z Tonle Sap wypływa rzeka o tej samej nazwie , która podczas pory suchej płynie na południe w kierunku Mekongu .
W czasie pory deszczowej nadmiar wody w Mekongu powoduje cofanie się wód rzeki w kierunku jeziora , które rozlewa się w centralnej części niziny .
Jego głębokość wzrasta wówczas z 1–3 m do 12 m. Z tego powodu zbiornik ten posiada jedne z najliczniejszych zasobów ryb słodkowodnych na świecie .
Doroczne wylewy wód Mekongu przyczyniają się także do akumulacji żyznych aluwiów na terenie równiny centralnej .
