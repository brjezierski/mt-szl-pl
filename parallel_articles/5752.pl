Kamela
Kamela ( dodatkowa nazwa w j. kaszub .
Kaméla , hist .
niem .
`` Kamehlen '' ) – wieś kaszubska w Polsce , położona w województwie pomorskim , w powiecie kartuskim , w gminie Somonino , 1,8 km na wschód od drogi krajowej nr 20 ze Stargardu do Gdyni .
W latach 1945-1998 ( także po 1975 ) wieś administracyjnie należała do województwa gdańskiego .
Historia .
Dawna wieś gburska , chłopska , pohutnicza .
Powstała około 1600 r. wraz z innymi wsiami założonymi na obszarach wytrzebionego na tym terenie lasu .
Pierwotnie miejscowość należała do starostwa skarszewskiego i sprawowała szarwarki dla utrzymania śluz młyńskich w Skarszewach , zamienione następnie na opłatę pieniężną .
Około 1880 r. wieś liczyła 1231 mórg upraw , 12 gospodarstw gburskich i 11 zagrodniczych .
Mieszkało tu 54 katolików i 119 ewangelików w 26 domostwach .
Kamela należała do parafii Goręczyno , szkoła znajdowała się w tym czasie na miejscu , a poczta w pobliskim Przywidzu .
Na miejscowym cmentarzu ewangelickim znajduje się pojedynczy , odrestaurowany krzyż żelazny .
Ostatni pochówek na tej nekropolii odbył się w roku 1946 .
