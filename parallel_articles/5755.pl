Kamenný Přívoz ( przystanek kolejowy )
Kamenný Přívoz – przystanek kolejowy w miejscowości Kamenný Přívoz , w kraju środkowoczeskim , w Czechach .
Znajduje się na wysokości 305 m n.p.m .
Jest zarządzany przez Správę železnic .
Na przystanku nie ma możliwości zakupu biletu , a obsługa podróżnych odbywa się w pociągu .
