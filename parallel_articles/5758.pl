Kamil Durczok
Kamil Sebastian Durczok ( ur .
6 marca 1968 w Katowicach , zm .
16 listopada 2021 tamże ) – polski dziennikarz , prezenter telewizyjny i radiowy oraz publicysta .
W latach 1993–2006 dziennikarz Telewizji Polskiej ( za zasługi w jej rozwoju w 2002 odznaczony Złotym Krzyżem Zasługi ) , w latach 2006–2015 dziennikarz telewizji TVN ; redaktor naczelny i prowadzący `` Faktów '' , w latach 2016–2017 gospodarz programu `` Brutalna prawda .
Durczok ujawnia '' ( później pod nazwą `` Durczokracja '' ) w Polsat News .
Laureat wielu nagród branżowych , m.in .
nagrody Grand Press dla dziennikarza roku ( 2000 ) , Złotej Telekamery w kategorii `` Informacje '' ( 2008 ) oraz Wiktora dla najwyżej cenionego dziennikarza , komentatora i publicysty ( 2004 ) .
Życiorys .
Wykształcenie i początki kariery .
Ukończył studia z zakresu komunikacji społecznej na Uniwersytecie Śląskim w Katowicach .
Studiował także prawo i filologię polską .
Podczas studiów odbywał praktyki robotnicze w Fabryce Samochodów Małolitrażowych w Tychach .
Pracę dziennikarską rozpoczął w 1991 w Radiu Katowice .
Wcześniej przez kilka lat współtworzył program Studenckiego Radia Egida działającego w domach studenta Uniwersytetu Śląskiego w Katowicach w dzielnicy Ligota .
W wieku 24 lat został dyrektorem i redaktorem naczelnym katowickiego radia TOP FM , należącego do NSZZ „ Solidarność ” .
Praca w TVP .
W latach 1993–2006 pracował w Telewizji Polskiej .
Swoją pracę w telewizji zaczął od współpracy z TVP Katowice , gdzie przeprowadzał wywiady z politykami w programie `` Obserwatorium '' .
Później aż do 2002 był prowadzącym lokalny program informacyjny TVP Katowice – `` Aktualności '' .
W 1996 zadebiutował na antenie ogólnopolskiej , gdzie został prowadzącym programy publicystyczne w TV Polonia takich jak : `` Studio parlamentarne '' oraz `` Przegląd publicystyczny '' .
We wrześniu 1998 został prowadzącym wtorkowego wydania programu publicystycznego `` W centrum uwagi '' , przekształconego w październiku 1999 w `` Monitor Wiadomości '' .
Program ten prowadził do listopada 2000 .
Od 15 marca 2001 do 7 lutego 2006 był prezenterem głównego wydania `` Wiadomości '' w TVP1 .
