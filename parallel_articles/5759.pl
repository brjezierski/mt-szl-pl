Kamil Glik
( ur .
3 lutego 1988 w Jastrzębiu-Zdroju ) – polski piłkarz , występujący na pozycji obrońcy we włoskim klubie Benevento Calcio oraz w reprezentacji Polski .
Uczestnik Mistrzostw Europy 2016 , 2020 oraz Mistrzostw Świata 2018 i 2022 .
Członek Klubu Wybitnego Reprezentanta .
Jeden z pięciu polskich piłkarzy obok Grzegorza Laty , Michała Żewłakowa , Roberta Lewandowskiego i Jakuba Błaszczykowskiego , który ma na koncie sto i więcej występów w reprezentacji .
Kariera klubowa .
Początki .
Pierwsze piłkarskie kroki stawiał w drużynie Miejskiego Ośrodka Sportu i Rekreacji Jastrzębie-Zdrój i Wodzisławskiej Szkole Piłkarskiej Wodzisław Śląski .
Po kilku latach spędzonych w WSP Wodzisław Śląski występował przez krótki okres na zasadzie wypożyczenia w lokalnej Silesii Lubomia .
Następnie razem z innym zawodnikiem WSP , Kamilem Wilczkiem , występował w hiszpańskim klubie UD Horadada , skąd przeszedł do drużyny Realu Madryt C , w której grał w sezonie 2007/08 .
Piast Gliwice .
