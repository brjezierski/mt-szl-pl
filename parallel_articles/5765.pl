Kamianka ( dopływ Bugu )
Kamianka ( nieoficjalna nazwa lokalna : Kamionka ) – rzeka w Polsce , prawy dopływ Bugu o długości 23,41 km .
Należy do dorzecza Bugu , do którego uchodzi w pobliżu mostu niedaleko wsi Turna Mała .
Rzeka ma swoje źródło w pobliżu miejscowości Krasewice-Czerepy , następnie przepływa m.in .
przez Czartajew oraz Siemiatycze , dzieląc to miasto na dwie zbliżone wielkością części .
W Czartajewie oraz w Siemiatyczach rzeka przepływa przez 2 sztuczne zbiorniki wodne .
W Siemiatyczach przyjmuje także 3 dopływy .
Są to lewobrzeżny Mahomet i prawobrzeżne Muchawiec oraz ciek bez nazwy .
Średni przepływ Kamianki w pobliżu ujścia do Bugu to 0,095 m³/s .
