Kamianki-Wańki
Kamianki-Wańki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Przesmyki .
Zaścianek szlachecki Wańki należący do okolicy zaściankowej Kamianka położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
