Kamień nad Odrą
Kamień nad Odrą – wyludniona wieś w województwie śląskim , w powiecie wodzisławskim , gminie Gorzyce .
Miejscowość tworzyła sołectwo `` Kamień '' .
W 1921 r. w pobliżu miejscowości miała miejsce bitwa .
Została wysiedlona z powodu budowy polderu Buków , który ma ochraniać przed zalaniem Racibórz , Opole i inne miejscowości położone nad Odrą .
Wieś przestała oficjalnie istnieć w 1999 roku .
