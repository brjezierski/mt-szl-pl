Węgiel kamienny
Węgiel kamienny – skała osadowa pochodzenia roślinnego , zawierająca 75−97 % pierwiastka węgla , powstała głównie w karbonie ( era paleozoiczna ) ze szczątków roślinnych , które bez dostępu tlenu uległy uwęgleniu .
Ma czarną barwę , matowy połysk , czarną rysę .
Węgiel kamienny stosowany jest powszechnie jako paliwo , choć jego udział w produkcji energii spada i prawdopodobnie będzie nadal spadał .
Jego wartość opałowa waha się od 16,7 do 29,3 MJ/kg i silnie zależy od jego składu ( zawartości popiołu , siarki , wilgotności ) .
Wartość opałowa czystego pierwiastka węgla wynosi ok. 33,2 MJ/kg .
Węgiel kamienny jest nieodnawialnym źródłem energii .
