Wydobycie węgla kamiennego w Polsce .
Polska jest największym producentem węgla kamiennego w Unii Europejskiej .
Węgiel kamienny wydobywano na Górnym Śląsku co najmniej od połowy XVII w. Działała wówczas kopalnia odkrywkowa w Murckach , co potwierdzają dokumenty z roku 1657 .
Wydobycie podziemne w tej kopalni rozpoczęto w roku 1755 .
Obecnie funkcjonuje ona jako Kopalnia Węgla Kamiennego Murcki-Staszic w Katowicach .
W roku 1767 w Małopolsce powstała kopalnia węgla kamiennego w Szczakowej ( obecnie dzielnica Jaworzna ) .
Węgiel jest w Polsce surowcem strategicznym , bo zaspokaja 60 % zapotrzebowania energetycznego kraju ( 2008 ) .
