Kamouraska ( regionalna gmina hrabstwa )
Kamouraska – regionalna gmina hrabstwa ( MRC ) w regionie administracyjnym Bas-Saint-Laurent prowincji Quebec , w Kanadzie .
Stolicą jest miasto Saint-Pascal .
Składa się z 19 gmin : 2 miast , 8 gmin , 7 parafii i 2 terytorium nie zorganizowanych .
Kamouraska ma 21 492 mieszkańców .
Język francuski jest językiem ojczystym dla 99,2 % , angielski dla 0,5 % mieszkańców ( 2011 ) .
