Kampala
Kampala – stolica i największe miasto Ugandy położone w południowej części kraju , w pobliżu Jeziora Wiktorii .
Kampala jest głównym ośrodkiem gospodarczym i kulturalnym kraju .
Miejscowość jest centrum handlu produktami rolnymi ( kawą , bawełną , herbatą , tytoniem , cukrem ) ; funkcjonują tu również zakłady przemysłu włókienniczego , metalowego , drzewnego , maszynowego i spożywczego .
W mieście działają : Ugandyjskie Towarzystwo Naukowe ( zał .
1933 ) , Uniwersytet Makerere ( zał .
1922 ) i wyższa szkoła techniczna oraz liczne instytucje kulturalne , m.in .
: muzeum narodowe i teatr narodowy .
35 km na południowy zachód od Kampali znajduje się międzynarodowy port lotniczy Entebbe , a w samym mieście znajduje się nieczynny port lotniczy Kampala .
