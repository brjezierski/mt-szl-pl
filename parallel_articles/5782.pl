Miasta partnerskie
Miasta partnerskie – forma współpracy między miastami w różnych krajach , która ma na celu wymianę kulturalną , gospodarczą i informacyjną .
W Europie formę tę powszechnie określa się terminami `` miasta bliźniacze '' , `` miasta przyjacielskie '' ( `` miasta zaprzyjaźnione '' ) lub `` miasta partnerskie '' .
Poza Europą ( szczególnie w krajach anglosaskich ) współpracę między miastami określa się pojęciem `` miasta siostrzane '' ( ang .
`` sister cities '' ) lub `` miasta bliźniacze '' ( ang .
`` twin towns '' lub `` twinned towns '' ) ; w krajach socjalistycznych są to `` miasta braterskie '' .
Historia partnerstwa miast .
Pierwsze partnerstwa miast powstawały w 1947 , by szerzyć międzynarodową współpracę u podstaw .
Jako pierwsze umowy partnerskie podpisały władze niemieckiego Ludwigsburga i francuskiego Montbéliard .
