Kamienica ( dopływ Wisłoki )
Kamienica – potok fliszowy , prawobrzeżny dopływ Wisłoki .
Płynie po terenie zajmowanym przez gminę Brzostek , przez Kamienicę Górną , Bączałkę , Smarżową , Siedliska-Bogusz , Gorzejową i Kamienicę Dolną .
Ma charakter podgórski .
Podobnie jak większość cieków w regionie , jest wcięty dość głęboko w pokrywy aluwialne .
Kamienica została uwzględniona w `` ocenie jakości wód rzeki Wisłoki '' z roku 2005 .
Ustalono , że jej woda zawiera azotyny oraz fosfor , a w ocenie zagrożenia nieosiągnięcia dobrego stanu wód sklasyfikowano ją jako potencjalnie zagrożoną .
