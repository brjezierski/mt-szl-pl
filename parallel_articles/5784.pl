Kamyszyn ( stacja kolejowa )
Kamyszyn ( ros : `` Камышин '' ) – stacja kolejowa w Kamyszynie , w obwodzie wołgogradzkim , w Rosji .
Jest zarządzana przez wołgogradzki oddział Kolei Nadwołżańskiej i znajduje się na 475 kilometrze linii Tambow – Kamyszyn .
Kamyszyn jest stacją czołową , skomunikowaną z drogami dojazdowymi do kombinatu produkcji szkła Kamyszinskij .
Liczba torów na stacji to 8 , liczba peronów to 2 .
Ze stacji rozpoczyna bieg pociąg nr 379/380 Kamyszyn - Moskwa - Kamyszyn .
Pod wiaduktem , na którym znajdują się tory stacyjne przebiega ulica Niekrasowa .
Ze stacji Kamyszyn prowadzą bocznice kolejowe do stacji zakładowej Kombinatskaja , która znajduje się w dzielnicy Nieftjanikow ( Kamyszyn ) .
