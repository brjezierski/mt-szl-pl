Kana ( miejscowość )
Kana ( arab .
قـانـا , `` Qana '' ) – miejscowość w południowym Libanie położona 10 km na południowy wschód od Tyru i 12 km na północ od granicy z Izraelem .
Większość z 10 tys .
mieszkańców jest szyitami , istnieje także społeczność chrześcijańska .
Według Biblii Jezus dokonał swojego cudu – przemiany wody w wino w Kanie Galilejskiej .
Miejscowi chrześcijanie wierzą , że obecna Kana jest miejscem gdzie wydarzył się cud opisywany w Piśmie Świętym .
Jednak w tej sprawie istnieje wiele sprzecznych opinii , według jednych zdarzenie biblijne miało miejsce w obecnej wsi Kafar niedaleko Nazaretu , zaś według innych była to miejscowość Kefar Kanna położona na północ od Nazaretu ( w Izraelu ) .
18 kwietnia 1996 r. Kana została ostrzelana po raz pierwszy przez wojska izraelskie .
Zginęło wtedy 106 cywilów , a 116 cywilów i 4 żołnierzy sił pokojowych ONZ zostało rannych .
30 lipca 2006 r. w wyniku nalotu bombowego zniszczony został budynek , w którym schroniła się ludność cywilna .
Co najmniej 28 osób zginęło , z czego 16 stanowiły dzieci .
