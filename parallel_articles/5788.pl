Kanał Mozambicki
Kanał Mozambicki ( fr .
`` Canal de Mozambique '' , port .
`` Canal de Moçambique '' , malg .
`` Mozambika Lakandranon ' i '' ) – cieśnina na Oceanie Indyjskim pomiędzy wybrzeżem Afryki a Madagaskarem .
Przez kanał przepływa ciepły Prąd Mozambicki ( odgałęzienie Prądu Południoworównikowego ) .
Główne porty nad kanałem to Beira w Mozambiku i Mahajanga na Madagaskarze .
