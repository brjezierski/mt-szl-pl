Kędzierzyn-Koźle
( , ) – miasto w Polsce , w województwie opolskim oraz siedziba powiatu kędzierzyńsko-kozielskiego , położone w południowo-wschodniej części Niziny Śląskiej , w Kotlinie Raciborskiej ( Niecka Kozielska ) na Górnym Śląsku , nad Odrą przy wylocie Kanału Gliwickiego i ujścia Kłodnicy .
Powstało w 1975 z połączenia czterech osobnych organizmów administracyjnych : Kędzierzyna , Koźla , Sławięcic i Kłodnicy .
W Koźlu znajduje się jeden z największych portów rzecznych w Polsce .
W Kędzierzynie-Koźlu znajdują się jedne z największych w Polsce zakładów chemicznych – Zakłady Azotowe Kędzierzyn , a także holding „ Blachownia ” .
Jest to drugie co do wielkości miasto województwa opolskiego pod względem liczby ludności i obszaru .
Według danych Urzędu Miasta z 31 grudnia 2022 miasto liczyło 52 723 mieszkańców , którzy byli zameldowani na pobyt stały .
Geografia .
Położenie .
Miasto leży w południowo-wschodniej części województwa opolskiego , w środkowej części mezoregionu Kotliny Raciborskiej ( 318.59 ) .
Mezoregion ten jest najdalej , wzdłuż biegu Odry , wysuniętą na południe częścią Niziny Śląskiej ( 318.5 ) .
Od wschodu sąsiaduje z Płaskowyżem Rybnickim , Wyżyną Katowicką i Garbem Tarnogórskim , od zachodu z Płaskowyżem Głubczyckim .
Na południu dolina Odry łączy Kotlinę Raciborską z Kotliną Ostrawską .
Według danych z 1 stycznia 2011 powierzchnia miasta wynosiła 123,4 km² .
Środowisko geograficzne .
Na obszarze Kędzierzyna-Koźla różnica wysokości terenu sięga 50 m. Najwyższy punkt znajduje się w lasach , przy wschodniej granicy miasta i osiąga 215,3 m n.p.m .. Teren obniża się ku dolinie Odry , a sama dolina wykazuje spadek w kierunku północnym .
Najniższy punkt znajduje się w miejscu , w którym Odra opuszcza miasto , a jego wysokość wynosi około 165 m n.p.m .. Dla miasta przyjmuje się średnią wysokość 180 m n.p.m .. Można dodać , że najwyższym stałym punktem miasta jest szczyt 150-metrowego komina E w Elektrowni Blachownia ( dzielnica Blachownia – ul .
Energetyków 11 ) , który jest jednocześnie najwyższą budowlą miasta , osiągający wysokość około 350 m n.p.m ..
Większość terenów miasta jest położona na dnie dolin rzecznych : Odry i jej prawobrzeżnego dopływu Kłodnicy .
Tereny położone na wschód od brzegów rzeki Odry są równinną terasą rzeczną .
Północno-wschodni skrawek miasta tworzą pokrywy lessowe , będące częścią grzbietu Chełma ( masyw Góry św .
Anny ) .
Wody .
Sieć rzeczną stanowi rzeka Odra i jej prawy dopływ Kłodnica ( ma swoje ujście do Odry na południowy wschód od centrum dzielnicy Koźle ) .
Oddzielną rolę w strukturze sieci hydrograficznej miasta zajmują sztuczne drogi wodne .
Dwa kanały wykorzystywały przebieg doliny naturalnego cieku Kłodnicy : Kanał Kłodnicki ( już nie funkcjonuje ) i Kanał Gliwicki .
Trzecim kanałem jest Kanał Kędzierzyński , będący odgałęzieniem od Kanału Gliwickiego do ZAK Spółka Akcyjna .
Kanał Odra-Dunaj-Łaba łączący Koźle z Wiedniem pozostał w sferze planów .
