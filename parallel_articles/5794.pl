Kansas City ( Missouri )
Kansas City – miasto w Stanach Zjednoczonych , w stanie Missouri , nad rzeką Missouri .
W 2019 roku liczy 495,3 tys .
mieszkańców , oraz ponad 2,1 mln na obszarze metropolitalnym , który rozlewa się na stan Kansas i obejmuje miasto Kansas ( w stanie Kansas ) .
Największe miasto w stanie Missouri .
Obejmuje obszar ponad 826,1 km² ( w tym 10,6 km² powierzchni wodnej ) .
Kansas City zostało założone w 1838 roku w miejscu zbiegu rzek Missouri oraz Kansas a prawa miejskie otrzymało w 1850 roku .
Jest siedzibą katolickiej diecezji Kansas City-St. Joseph , której biskupem jest Robert W. Finn .
