Standardowy język kantoński
Standardowy język kantoński , yue – jeden z języków chińskich używany na południu Chin , w Hongkongu i Makau , a także przez potomków chińskich emigrantów w Azji Południowo-Wschodniej i innych rejonach świata .
Jego nazwa wywodzi się od prowincji Guangdong .
Liczbę użytkowników kantońskiego szacuje się ogółem na 55 mln ( z uwzględnieniem wszystkich pokrewnych dialektów z grupy yue ) .
