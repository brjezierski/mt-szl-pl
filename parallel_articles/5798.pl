Kanye
Kanye – miasto w południowej Botswanie , ośrodek administracyjny Dystryktu Southern .
Liczy 44718 mieszkańców ( 2005 ) .
Jest również stolicą terytorium plemiennego Bangwaketse , wodzem tego plemienia jest Kgosi Seepapitso IV .
Kanye jest podzielone na trzy okręgi wyborcze , podział odnosi się do wyborów Zgromadzenia Narodowego Botswany .
Noszą one nazwy Kanye Północne , Kanye Południowe i Kanye Wschodnie .
Nie ma natomiast okręgu Kanye Zachodnie .
