Karp ( imię )
Karp – imię męskie pochodzenia biblijnego .
Wywodzi się ze zlatynizowanej formy `` Carpus '' [ `` Karpus '' ] lub greckiej `` Καρπος '' [ `` Kárpos '' ] i oznacza `` owoc '' lub `` zysk '' .
W Polsce spotyka się przeważnie u wiernych Kościoła wschodniego .
