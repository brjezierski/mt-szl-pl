Kapusta warzywna głowiasta
Kapusta warzywna głowiasta , popularnie nazywana kapustą głowiastą ( `` Brassica oleracea '' L. var .
`` capitata '' L. ) – odmiana kapusty warzywnej .
Jest to roślina dwuletnia należąca do rodziny kapustowatych .
Nie występuje w stanie dzikim , jedynie w uprawie .
