Taczka
Taczka , taczki – mały wózek z jednym kołem , częściowo unoszony ręcznie , używany do transportu przedmiotów lub materiałów sypkich na niewielkich odległościach .
Taczki wyposażone są w dwa uchwyty , przy pomocy których są unoszone i pchane , rzadziej ciągnięte , przez człowieka .
Wykonywane są z drewna , metalu , plastiku lub kombinacji tych materiałów .
Znajdują szerokie zastosowanie w ogrodnictwie i na placach budowy .
Wyraz `` taczka `` jest regionalnym plurale tantum , tj .
wyrazem w niektórych regionach stosowanym wyłącznie w liczbie mnogiej , jako `` taczki '' .
Taczka znana była w Chinach od ok. 230 roku ( według tradycji wynalazł ją wówczas Zhuge Liang ) .
W Europie używana od XII wieku .
Została zapewne przyswojona poprzez Bliski Wschód w następstwie wypraw krzyżowych .
