Karaczajowie
Karaczajowie – jeden z ludów tureckich zamieszkujący autonomiczną republikę Karaczajo-Czerkieską w północnej części Kaukazu , w Federacji Rosyjskiej .
Największym skupiskiem Karaczajów jest Karaczajewsk – drugie miasto pod względem liczby mieszkańców ( po stolicy republiki – Czerkiesku ) , będące centrum administracyjnym rejonu karaczajewskiego i siedzibą Państwowego Uniwersytetu Karaczajewo-Czerkieskiego .
Naród liczący około 150–160 tys .
