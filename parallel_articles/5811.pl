Od początku XIX wieku zamieszkane przez nich ziemie należą do Rosji .
W 1943 wszyscy mieszkańcy republiki zostali deportowani z Kaukazu i powrócili dopiero po śmierci Stalina .
Tradycyjnymi zajęciami było pasterstwo , rolnictwo i rzemiosło .
