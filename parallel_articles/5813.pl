Karaganda
Karaganda ( , `` Karagandy '' ; , `` Karaganda '' ) – miasto w Kazachstanie , w Karagandyjskim Zagłębiu Węglowym ; stolica obwodu karagandyjskiego .
Ośrodek wydobycia węgla kamiennego , przemysłów : maszynowego , chemicznego , materiałów budowlanych i spożywczego .
W mieście znajduje się kilka szkół wyższych , m.in .
uniwersytet ( zał .
w 1972 roku ) , uczelnia medyczna i politechnika ; ponadto kilka instytutów badawczych , muzeum , teatry i ogród botaniczny .
Miasto jest siedzibą Związku Polaków w Kazachstanie ( zał .
w 1994 roku ) .
Od 1999 roku Karaganda jest także siedzibą katolickiej diecezji karagandyjskiej – jednej z czterech jednostek terytorialnych Kościoła na terenie Kazachstanu .
Historia .
Pierwsze osadnictwo na terenie miasta pojawiło się w 1856 roku .
Rok później rozpoczęto eksploatację węgla kamiennego na małą skalę , w który zaopatrywano pobliską hutę miedzi .
