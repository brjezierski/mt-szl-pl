Język karaimski
Język karaimski ( karaim .
, / ) – język z kipczackiej grupy języków tureckich , którym posługują się Karaimi zamieszkujący Litwę ( głównie miasta Troki i Wilno ) , Krym i Polskę .
Zarys historii języka .
Język karaimski dzielony jest tradycyjnie na dwie grupy : wschodnią ( na Krymie ) i zachodnią z dialektami trocko-wileńskim i łucko-halickim .
Wspomniane dwa dialekty z grupy zachodniej zachowały wiele cech archaicznych , świadczących o kipczackim pochodzeniu języka .
Na karaimski bardzo silny wpływ miał krymskotatarski , a także osmański .
Różnice historyczne są głównym powodem rozróżnienia między wschodnio- a zachodniokaraimską odmianą języka .
Nigdy się też nie wykształcił wspólny karaimski język literacki .
