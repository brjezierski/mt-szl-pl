Język karakałpacki
Język karakałpacki – język turkijski z grupy kipczackiej , używany głównie przez Karakałpaków mieszkających w Karakałpacji ( Uzbekistan ) , gdzie jest językiem urzędowym , obok uzbeckiego .
Karakałpacy z pozostałej części Uzbekistanu używają w większej mierze języka uzbeckiego .
Posługuje się nim także około 2000 mieszkańców Afganistanu .
Rozpowszechniony jest również wśród mniejszych grup w
Rosji , Kazachstanie , Turcji i w innych częściach świata .
Zazwyczaj wyróżnia się dwa dialekty – północno-wschodni i południowo-wschodni , czasem rozważa się także dialekt
używany w Kotlinie Fergańskiej .
