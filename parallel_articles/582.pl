2 Girls 1 Cup
2 Girls 1 Cup ( pol .
„ Dwie dziewczyny , jeden kubek ” ) – nieoficjalna nazwa zwiastuna z filmu pornograficznego `` Hungry Bitches '' , wyprodukowanego w 2007 roku przez brazylijską wytwórnię MFX Media .
Widać w nim dwie kobiety uprawiające koprofilię i emetofilię .
Tło muzyczne stanowi utwór „ Lovers Theme ” autorstwa Hervé Roya , pierwotnie skomponowany dla filmu `` Mania wielkości '' .
Zwiastun , trwający około minuty , stał się memem internetowym .
W wielu serwisach społeczność zamieszczała fotografie i filmy przedstawiające reakcje ludzi , którzy oglądali go po raz pierwszy .
Zwiastun doczekał się także kilku parodii , jak na przykład „ 2 Guys 1 Cup ” , „ 2 Girls , 1 Cup : The Show ” czy „ 1 guy 1 lunchable ” .
