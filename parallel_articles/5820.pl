Język karatajski
Język karatajski ( nazwa własna : КIирли мицIи ) – jeden z niewielkich języków kaukaskich , używany przez Karatajów .
Należy do języków andyjskich w zespole awaro-didojskim , tworzącym podgrupę wśród języków dagestańskich w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Jest blisko spokrewniony z językiem achwaskim .
Język ten używany jest według różnych szacunków od ok. 5 tys .
osób ( 1990 r. ) do 6 tys .
( według spisu powszechnego w 2000 r. ) , w osiedlu Karata ( południowy Dagestan ) oraz kilku innych , niewielkich wioskach w okolicy .
Nazwa języka wywodzi się od nazwy największej wsi , w której zamieszkują użytkownicy języka .
Język ten nie wykształcił piśmiennictwa .
Jest używany wyłącznie w sytuacjach nieformalnych , w domu , wśród przyjaciół .
W charakterze języka literackiego używany jest język awarski , jako największy język literacki Dagestanu .
Niektórzy Karatajowie znają także rosyjski , jako język urzędowy Federacji Rosyjskiej .
