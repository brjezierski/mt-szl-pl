Karaś ( powiat iławski )
Karaś – jezioro położone w województwie warmińsko-mazurskim , w powiecie iławskim , w gminie Iława oraz częściowo w powiecie nowomiejskim , w gminie Biskupiec .
Leży na obszarze Pojezierza Iławskiego .
W systemie gospodarki wodnej jest jednolitą częścią wód Karaś o kodzie PLLW20575 .
Reprezentuje typ 3b , czyli jezioro o wysokiej zawartości wapnia , o dużym wypływie zlewni , niestratyfikowane .
Należy do regionu wodnego Dolnej Wisły , podlegając regionalnemu zarządowi gospodarki wodnej PGW WP w Gdańsku , od 2018 zarządowi zlewni w Tczewie .
