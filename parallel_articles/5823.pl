Kardynał
Kardynał ( – główny , zasadniczy , mocno z czymś związany ) , formalnie Kardynał Świętego Kościoła Rzymskiego ( łac .
`` Cardinalis Sanctae Romanae Ecclesiae '' ) – najwyższa po papieżu godność kościelna w Kościele katolickim .
Wszyscy kardynałowie razem tworzą Kolegium Kardynałów .
Nominacje .
Kardynałów mianuje papież , wybierając ich spośród mężczyzn , którzy mają przynajmniej święcenia prezbiteratu .
Papież dokonuje promocji mianowanych kardynałów na konsystorzu .
Kardynałowie są mianowani przez papieża według jego własnego uznania , po zasięgnięciu opinii członków Kolegium Kardynalskiego na temat kandydatów .
Gdyby zamianowany kardynał nie miał święceń biskupich , powinien je przyjąć , choć nie jest to bezwzględnie konieczne , to znaczy nie stanowi przeszkody w wykonywaniu jakichkolwiek uprawnień kardynalskich .
W przeszłości kardynałami w stopniu kardynałów diakonów mianowano nie kapłanów , ale diakonów , a nawet duchownych niższych .
Ostatnim kardynałem diakonem bez święceń kapłańskich był Teodolfo Mertel .
Polscy biskupi otrzymali w połowie XV wieku zakaz starania się o godność kardynalską bez wyraźnej zgody senatu oraz króla , ponieważ posiadanie kapelusza kardynalskiego przez któregoś z polskich biskupów było przyczyną bardzo ostrych sporów o precedencję z prymasami Polski .
Dodatkowo dawniej godność ta była uważana za obcą i niebezpieczną .
