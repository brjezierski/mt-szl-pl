Karel Gott
Karel Gott ( ur .
14 lipca 1939 w Pilźnie , zm .
1 października 2019 w Pradze ) – czeski piosenkarz .
Uznawany za najbardziej znanego czeskiego ( wcześniej czechosłowackiego ) piosenkarza w historii .
W karierze został 42 razy nagrodzony nagrodą Złotego/Czeskiego Słowika ( 1962–1991 jako Złoty Słowik , od 1996 jako Czeski Słowik ) .
Trzykrotnie nagrodzony ( w 1982 , 1984 i 1995 ) .
Jego głos klasyfikowany był jako tenor .
Śpiewał między innymi piosenki pop , arie operowe i operetkowe , adaptacje klasycznych pieśni , narodowych i ludowych piosenek ( folklor ) , aranżacje melodii z musicali i piosenki country .
Szczególnie znane jest jego wykonanie arii z `` West Side Story '' – `` Maria '' .
Wykonał lub adaptował w języku czeskim wiele światowych przebojów m.in .
`` El condor pasa '' , `` By the Rivers of Babylon '' , `` Hot Stuff '' .
Twórczość muzyka klasyfikowana jest jako opera , jazz , pop , rock , adult contemporary , world music , szlagiery , pop-rock , country , a także muzyka klasyczna .
W latach 2006–2009 w Jevanach pod Pragą , w dawnej willi piosenkarza istniało poświęcone mu muzeum , `` Gottland '' .
Biografia .
Jego rodzina jest pochodzenia niemieckiego .
Urodził się Pilźnie , gdy miał 6 lat rodzina przeprowadziła się do Pragi .
Nie dostał się na studia do praskiej Akademii Sztuki , Architektury i Designu ( UMPRUM ) i zaczął pracować jako elektryk .
Wcześnie zaczął interesować się muzyką jazzową , próbował uczyć się gry na gitarze i kontrabasie , ale postanowił skoncentrować się na śpiewie i zaczął brać prywatne lekcje .
W latach 50. amatorsko występował i uczestniczył w konkursach piosenkarskich .
Kariera artystyczna .
Karierę zaczął zdobywając angaż w praskiej kawiarni nad Wełtawą w 1958 .
W 1960 r. rozpoczął studia w praskim Konserwatorium , pod kierunkiem Konstantina Karenina ( ucznia Fiodora Szalapina ) .
Pierwsze koncerty zagraniczne odbył w Polsce z Jazzową Orkiestrą Czechosłowackiego Radia .
