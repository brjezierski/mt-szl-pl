Karistos
Karistos ( gr .
Κάρυστος , staroż .
`` Karystos '' ) - miejscowość w Grecji , na wyspie Eubei , w administracji zdecentralizowanej Tesalia-Grecja Środkowa , w regionie Grecja Środkowa , w jednostce regionalnej Eubea .
Siedziba gminy Karistos .
W 2011 roku liczyła 5112 mieszkańców .
Port w Rafinie umożliwia połączenia promowe z Atenami , dzięki czemu miasto posiada bezpośrednie połączenie ze stolicą Grecji .
Miasto przeszło urbanizacje w XIX wieku , a plany jej przeprowadzenia wykonał bawarski architekt Bierbach .
