Karol Godula
Karol Godula ( polska pisownia ) , Karl Godulla ( niemiecka pisownia ) , Carolus Godulla ( ur .
8 listopada 1781 , zm .
6 lipca 1848 we Wrocławiu ) – górnośląski przedsiębiorca .
Godula położył znaczne zasługi dla rozwoju gospodarczego Górnego Śląska .
Życiorys .
Został ochrzczony w drewnianym kościele w Przyszowicach ( obecnie , przeniesiony , znajduje się w Borowej Wsi ) .
Początkowo chodził do ludowej szkoły w Przyszowicach , później w 1793 r. ze względu na wybitne uzdolnienie dziecka rodzice przenieśli go do gimnazjum cystersów w Rudach Wielkich .
Następnie uczył się od 1798 r. w liceum w Opawie .
W 1801 r. Karol Godula rozpoczął pracę u hrabiego von Ballestrema .
Dzięki swojej inteligencji szybko awansował na zarządcę majątku hrabiego .
W 1809 r. von Ballestrem nadał mu tytuł „ Amtmanna ” ( ekonoma ) , a 5 lat później mianował go głównym ekonomem swoich dóbr .
Godula w młodym wieku padł ofiarą brutalnej napaści .
Został wówczas nie tylko ciężko pobity , ale i wykastrowany ; stracił władzę w jednej ręce i miał od tej pory problemy z poruszaniem się .
W 1812 r. rozpoczęto budowę huty cynku „ Karlhütte ” .
Hutą zarządzał Godula , a w 1815 r. dostał od swojego pracodawcy 28 kuksów ( udziałów ) w hucie .
W 1828 r. otrzymał drugie tyle kuksów w hucie .
Karol Godula zaczął pracę na własny rachunek .
Pierwszą kopalnię „ Maria ” Karol Godula zbudował w roku 1823 .
Kopalnia zajmowała się wydobyciem galmanu , a działała do 1895 r. Dzięki zarobionym pieniądzom Godula kupił dobra ziemskie : Orzegów , Szombierki , Bobrek , Bujaków .
Zakupił także kopalnie „ Stein ” i „ Rosalie ” oraz huty „ Morgenroth ” i „ Gute Hoffnung ” .
Potem do swojego majątku włączył Paniowy .
W latach 20 .
XIX wieku kryzys przeżywały huty cynku .
Wielu przedsiębiorców zbankrutowało , jednak Karolowi Goduli udało się wtedy zwiększyć swój majątek .
W dniu śmierci Karola Goduli , jego majątek szacowano na 2 miliony talarów .
Na majątek składało się : 19 kopalń galmanu , 40 kopalń węgla kamiennego , 3 huty cynku , 28 kuksów w hucie „ Karol ” , do tego dochodzą tereny : Orzegów , Szombierki , Bujaków , Bobrek , Paniowy .
Dla porównania średnia pensja nauczycielki ( bardzo dobrze płatna ) rocznie wynosiła około 300 talarów .
Karol Godula zmarł 6 lipca 1848 roku we Wrocławiu , został pochowany w krypcie pod kościołem pw .
Najświętszego Serca Pana Jezusa w Bytomiu-Szombierkach .
Spuścizna .
Godula praktycznie cały swój majątek zapisał w testamencie niespokrewnionej z nim swojej wychowanicy Joannie Gryzik ( wraz z jej ręką majątek odziedziczyła rodzina hrabiów von Schaffgotsch ) .
W 1909 r. Joanna przeniosła jego prochy z Wrocławia , w którym zmarł , do Szombierek .
Dzielnica Rudy Śląskiej ( Godula , niem .
`` Godullahütte '' ) jest nazwana jego nazwiskiem .
Karol Godula jest patronem Górnośląskiej Wyższej Szkoły Przedsiębiorczości w Chorzowie , Gimnazjum nr 6 w Bytomiu w dzielnicy Szombierki , Zespołu Szkół Ponadgimnazjalnych nr 3 w Rudzie Śląskiej Wirku , Szkoły Podstawowej nr 40 w Rudzie Śląskiej , a także Centrum Edukacji Ekonomiczno-Handlowej w Tarnowskich Górach .
Karol Godula miał pałac w Szombierkach , obecnie w tym miejscu znajduje się Szkoła Podstawowa nr 45 i Przedszkole nr 39 .
