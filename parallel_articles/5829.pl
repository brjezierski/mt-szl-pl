Karl Malden
Karl Malden , właśc .
Mladen George Sekulovich , serb .
Младен Ђорђе Секуловић ( ur .
22 marca 1912 w Chicago , zm .
1 lipca 2009 w Los Angeles ) – amerykański aktor filmowy pochodzenia serbsko-czeskiego .
Laureat Oscara za drugoplanową rolę w filmie `` Tramwaj zwany pożądaniem '' ( 1951 ; reż .
Elia Kazan ) .
Młodość .
Ojciec aktora , Petar Sekulović ( 1886-1975 ) był Serbem urodzonym w Bileci ; na początku XX wieku wyemigrował do Chicago .
Tam ożenił się z Minnie Seberą ( 1892-1995 ) , robotnicą z fabryki tekstylnej , z pochodzenia Czeszką .
Karl , najstarszy z trójki braci , urodził się w Chicago , choć dzieciństwo i młodość spędził w hutniczym miasteczku Gary w stanie Indiana .
Po ukończeniu `` Emerson High School '' w Gary pracował z ojcem w tamtejszej fabryce stali .
Jednak po kilku latach porzucił pracę , by poświęcić się aktorstwu .
Ukończył Goodman School of Drama na Uniwersytecie DePaul .
