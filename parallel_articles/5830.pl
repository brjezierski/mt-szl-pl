Karl Marx
Karl Marx ( pol .
Karol Marks ; ur .
5 maja 1818 w Trewirze , zm .
14 marca 1883 w Londynie ) – niemiecki filozof pochodzenia żydowskiego , socjolog , ekonomista , historyk , dziennikarz i działacz rewolucyjny .
Twórca socjalizmu naukowego , współzałożyciel I Międzynarodówki .
Życiorys .
Młodość i rodzina .
Pochodził ze zasymilowanej rodziny żydowskiej , pochodzenia aszkenazyjskiego .
Jego dziadek Meier Halewi Marx był rabinem w Holandii , pełniąc tę funkcję od 1723 roku .
Jako pierwszy z rodziny świecką edukację otrzymał Heinrich Marx , ojciec Karla .
Heinrich , aby uciec od antysemickich ograniczeń prawnych , z judaizmu przeszedł na dominujący w Niemczech luteranizm , zmieniając wówczas imię z pochodzącego z języka jidysz Herschela na niemieckiego Heinricha .
Ojciec Karla popierał liberalizm klasyczny i zainteresował się ideami filozofów Imanuela Kanta oraz Woltera .
Brał udział w agitacji na rzecz wprowadzenia w Prusach konstytucji i reform , które ograniczyłyby monarchię absolutną .
W 1815 roku rozpoczął karierę adwokacką , a w 1819 roku wraz z rodziną wprowadził się do dziesięciopokojowego domu przy Simeongasse ( obecnie Simeonstrasse 8 ) w pobliżu Porta Nigra .
Jego żona , Henrietta Pressburg była holenderską Żydówką , która pozostała przy judaizmie .
Henrietta była bardzo opiekuńcza i religijna , co nie pozostało bez wpływu na jej dzieci .
Karl urodził się 5 maja 1818 roku w Trewirze , w budynku 664 przy Brückergasse .
W 1928 roku lokal został zakupiony przez Socjaldemokratyczną Partię Niemiec , a współcześnie mieści się tam muzeum poświęcone Marksowi .
Niewiele wiadomo o dzieciństwie Karla .
Był trzecim z dziewięciorga dzieci , najstarszym synem stał się , gdy w 1819 roku zmarł jego brat Moritz .
