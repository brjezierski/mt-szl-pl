Języki wschodniotureckie
Języki wschodniotureckie , także południowowschodniotureckie , karłuckie lub karachanidzkie – jedna z głównych gałęzi języków turkijskich .
W jej skład wchodzą m.in .
: uzbecki , ujgurski i czagatajski .
Stanowią połączenie dwóch historycznie i geograficznie pokrewnych , jednak możliwe że różnych pod względem pochodzenia , grup języków : wschodniej karłucko-ujgurskiej i zachodniej karłucko-chorezmijskiej ( czagatajskiej ) .
Obie grupy znajdowały się w ciągłym kontakcie , w wyniku czego istnieje między nimi rząd osobliwych izoglos .
Podział obu proponował Nikołaj Baskakow , jednak w praktyce często nie jest uznawany – obie grupy są przedstawiane jako jedna lub karłucko-ujgurskie ( wszystkie lub niektóre ) wyłącza się z niej i opisuje jako ujgursko-oguzyjskie lub ujgursko-tukjujskie .
Według klasyfikacji Olega Mudraka staroturecki , staroujgurski i chaładż ( arghu ) oraz współczesne karłuckie stanowią jedną całość .
