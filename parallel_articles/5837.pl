Karminadle
Karminadle ( karminadla , karbinadle , karbinadla , kardinadle , kardinadla , bergi ) – potrawa kuchni śląskiej , rodzaj kotletów mielonych .
Są to smażone na głębokim tłuszczu kulki z mielonego mięsa , tradycyjnie wieprzowego .
Gotowe karminadle mają lekko spłaszczony , kulisty kształt i średnicę ok. 5–7 cm .
W smaku są słone i lekko pikantne .
17 stycznia 2008 tak zdefiniowane karminadle zostały wpisane na `` Listę produktów tradycyjnych '' Ministerstwa Rolnictwa i Rozwoju Wsi w kategorii „ Gotowe dania i potrawy w województwie śląskim ” .
Tradycyjne karminadle przygotowuje z wieprzowiny ( łopatka lub karczek ) zmielonej razem z czerstwą bułką namoczoną w mleku lub wodzie , wymieszanych z drobno pokrojoną cebulą , surowym jajkiem i przyprawami ( solą i pieprzem ) .
Uformowane kotlety obtacza się w bułce tartej i smaży na głębokim tłuszczu ( smalcu ) z obu stron .
W przeszłości karminadle były daniem odświętnym ( przyrządzanym np .
po świniobiciu ) , później stały się daniem obiadowym podawanym w tygodniu .
Do historii polskiej kinematografii przeszła scena z rozgrywającego się na Śląsku w 1934 roku filmu `` Perła w koronie '' ( 1971 ) , w której główny bohater po powrocie do domu z pracy w kopalni otrzymuje na obiad ( na drugie danie ) ziemniaki z karminadlem i kapustą .
Karminadle podaje się np .
z kluskami śląskimi i gotowaną młodą kapustą , lub też np .
z ziemniakami i surówką ( z kapusty lub buraków ) .
Pokrojone na plastry karminadle spożywane są również na zimno , jako składnik kanapek .
