Karol I Stuart
Karol I Stuart ( ur .
19 listopada 1600 w Dunfermline , Fife ; zm .
30 stycznia 1649 w Londynie ) – król Anglii i Szkocji w latach 1625-1649 , syn Jakuba I Stuarta , króla Anglii i Szkocji ( jako Jakub VI ) oraz Anny , córki króla Danii Fryderyka II .
Karol był jedynym brytyjskim władcą , który został obalony przez poddanych i ścięty .
Droga do tronu .
Książę Yorku .
Drugi syn króla Szkocji przyszedł na świat w szkockiej rezydencji Dunfermline , w Fife w 1600 r. Karol był słabo rozwiniętym dzieckiem .
Do trzeciego roku życia nie potrafił chodzić i mówić .
W 1603 r. , gdy zmarła królowa Anglii Elżbieta I , ojciec Karola został nowym królem Anglii .
Kiedy Jakub wyruszył na południe , towarzyszyła mu większość rodziny .
W Szkocji , pod opieką pielęgniarek , pozostał tylko Karol , gdyż obawiano się , że podróż będzie zbyt niebezpieczna dla chorowitego królewicza .
Do Anglii Karol wyruszył w lipcu 1604 r. Znajdował się pod opieką pielęgniarki Alletty Carey , z pochodzenia Holenderki , która uczyła go chodzić i mówić .
Nalegała również , aby Karol nosił buty z hiszpańskiej skóry , wzmacniane mosiądzem , po to , by wzmacniały słabe kostki księcia .
Karol był bardzo niskim dzieckiem , nawet jako dorosły człowiek mierzył tylko 5 stóp i 4 cale ( 162 cm ) .
