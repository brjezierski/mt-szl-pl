Karol Miarka ( starszy )
Karol Miarka zwany Starszym lub Ojcem ( ur .
22 października 1825 w Pielgrzymowicach , zm .
15 sierpnia 1882 w Cieszynie ) – polski działacz społeczny na Górnym Śląsku , nauczyciel , pisarz , publicysta i drukarz .
Inicjator utworzenia i organizator wielu polskich organizacji społecznych oraz gospodarczych .
Życiorys .
Syn Antoniego , nauczyciela , i Karoliny z Borówków .
Ojciec Karola zwanego młodszym – śląskiego pisarza , publicysty oraz wydawcy .
Początkowo uczył się w szkole ojca w Pielgrzymowicach , później w gimnazjach w Pszczynie i Gliwicach .
W 1846 r. ukończył seminarium nauczycielskie w Głogówku , po którego ukończeniu był nauczycielem ludowym w Lędzinach , Urbanowicach , Piotrowicach .
Od 1850 r. pełnił funkcję nauczyciela w Pielgrzymowicach .
Był także organistą , następnie pisarzem gminnym i sędzią polubownym .
