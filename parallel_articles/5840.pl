Karolin ( rejon mostowski )
Karolin ( ; ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie mostowskim , w sielsowiecie Dubno .
Współczesna miejscowość obejmuje także dawną osadę Woronowszczyznę .
W dwudziestoleciu międzywojennym kolonia Karolin i osada Woronowszczyzna leżały w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Dubno .
W 1921 Karolin zamieszkiwało 13 osób , a Woronowszczyznę 6 osób .
Cała populacja obu miejscowości była Białorusinami wyznania prawosławnego .
