Mapa
Mapa ( z łac .
`` mappa '' – obrus ) – graficzny obraz powierzchni Ziemi lub jej części ( także nieba lub planety czy innego ciała niebieskiego ) , wykonywany na płaszczyźnie , w skali , według zasad odwzorowania kartograficznego , przy użyciu umownych znaków graficznych .
Mapa stanowi podstawowe narzędzie badań i prezentacji wyników w historii , geografii i geodezji .
Przeniesienie powierzchni ze sfery ( Ziemia nie jest idealną kulą , ma nieregularny kształt geoidy , ale przy sporządzaniu mapy przyjmuje się założenie o jej kulistości , lub że jest elipsoidą obrotową ) na płaską powierzchnię mapy wymaga :
Nauka o mapach to kartografia , zaś znaki kartograficzne to symbole , za pomocą których wyraża się treść mapy .
