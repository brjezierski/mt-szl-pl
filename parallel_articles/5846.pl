Mapa Helwiga
Mapa Helwiga ( ) − poglądowa mapa Śląska sporządzona w XVI wieku przez Martina Helwiga .
Martin Helwig sporządził mapę na podstawie własnych obserwacji , a także opowieści lokalnej ludności .
Mapa po raz pierwszy została wydana w Nysie w 1561 roku .
Później wielokrotnie wznawiana ( 1605 , 1627 , 1642 , 1685 , 1738 , 1745 , 1746 , 1765 , 1776 dwukrotnie i 1778 ) , aż do połowy XVIII wieku stanowiła podstawowe źródło informacji kartograficznej o tym obszarze .
Mapa jest w skali ok. 1:540 000 .
Wymiary mapy : 57,5 × 73 cm ( z ramką : 67 × 81,5 cm ) .
Przedstawia tereny od Krakowa do Bischofswerda i od Poznania do Ołomuńca .
Mapa jest zorientowana na południe .
Znajduje się na niej przeszło 370 lokalizacji .
Ich rozmieszczenie pokrywa się z mapą Europy z 1554 roku autorstwa Merkatora .
Już w 1570 znalazła się w pierwszym wydaniu atlasu Abrahama Orteliusa „ Theatrum orbis terrarum ” .
