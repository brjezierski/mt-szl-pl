Kartuzy
Kartuzy ( dodatkowa nazwa w Kartuzë , ) – kaszubskie miasto w województwie pomorskim , siedziba powiatu kartuskiego i gminy miejsko-wiejskiej Kartuzy .
Miasto leży nad czterema jeziorami zwanymi kartuskimi .
Są to : jezioro Karczemne , Klasztorne Duże , Klasztorne Małe i Mielenko .
W Kartuzach krzyżują się drogi wojewódzkie : nr 211 , nr 224 i nr 228 .
Według danych GUS z 30 czerwca 2021 r. , Kartuzy liczyły 14 186 mieszkańców i były pod względem liczby ludności osiemnastym miastem w województwie pomorskim .
Położenie .
Według danych z 1 stycznia 2010 powierzchnia miasta wynosi 6,80 km² .
Kartuzy leżą na Kaszubach , historycznie na Pomorzu Gdańskim .
W latach 1975–1998 miasto administracyjnie należało do woj .
gdańskiego .
Po 1999 Kartuzy leżą w województwie pomorskim i są stolicą powiatu kartuskiego .
Historia .
Neolit .
Prace archeologiczne na terenie Kartuz zaczęły się w XIX wieku .
Większość ze znalezisk została jednak stracona podczas II wojny światowej .
Kolejne prace mające na celu odtworzenie historii Kartuz były wykonywane w latach 70 .
XX wieku .
Rejon Kartuz został zasiedlony prawdopodobnie później niż tereny znajdujące się na południe .
Przez okres plejstocenu teren ten był zajęty przez lądolód .
W okresach interglacjałów pojawiały się na Pomorzu różne grupy ludności koczowniczej , wędrujące na północ za zwierzyną , brak jednak znalezisk potwierdzających bytność takich grup na terenie przyszłego miasta .
