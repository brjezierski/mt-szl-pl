Sałatka ziemniaczana
Sałatka ziemniaczana ( ) – potrawa kuchni niemieckich oraz kuchni pozostających pod wpływami niemieckimi .
Sałatka przygotowywana jest przede wszystkim z ugotowanych i pokrojonych w kostkę ziemniaków oraz z posiekanej cebuli .
Doprawiana jest olejem i octem ( sokiem z cytryny ) lub majonezem oraz solą i pieprzem .
Sałatka ziemniaczana w wersji podstawowej , zaliczana jest do potraw postnych .
Inne opcjonalne dodatki to : marchew , pietruszka , seler korzeniowy , ogórki marynowane lub kiszone , a nawet kiełbasa , boczek wędzony lub filety śledziowe .
Wersję doprawioną majonezem przygotowuje się zwykle dzień przed podaniem .
Przepis na sałatkę ziemniaczaną podał Jan Milikowski w wydanej przez siebie książce `` Potrawy z kartofli '' ( 1842 ) .
Lucyna Ćwierczakiewiczowa w swojej książce kucharskiej `` 365 obiadów '' ( 1860 ) podała przepis na „ Sałatę z kartofli ze śledziami ” .
Na potrawę składały się ziemniaki ugotowane w mundurkach , niemaczane śledzie , ogórki konserwowe ( korniszony ) , cebula , oliwa , ocet , sól , pieprz i cukier .
Kartoffelsalat na liście polskich produktów tradycyjnych .
Sałatka o nazwie „ Kartoffelsalat ” została wpisana 28 sierpnia 2006 na `` Listę produktów tradycyjnych '' Ministerstwa Rolnictwa i Rozwoju Wsi w kategorii „ Gotowe dania i potrawy ” w województwie opolskim .
Potrawa ta bywa też nazywana „ szałotem śląskim ” lub „ szałotem kartoflanym z boczkiem wędzonym ” .
