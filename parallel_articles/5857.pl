Kashima ( Ibaraki )
– miasto portowe w Japonii , w prefekturze Ibaraki , położone nad Pacyfikiem .
Miasto jest centralną częścią pasa przemysłowego Kashima-Rinkai .
Ma powierzchnię 106,02 km2 .
W 2020 r. mieszkało w nim 66 965 osób , w 28 230 gospodarstwach domowych ( w 2010 r. 66 030 osób , w 25 222 gospodarstwach domowych ) .
