Morze Kaspijskie
Morze Kaspijskie ( , `` Darja-je Chazar '' ; , `` Kaspijskoje morie '' ; ; , `` Kaspij tengyzy '' ; ) – bezodpływowe słone jezioro reliktowe w Azji i w części północnej w Europie .
Jest największym jeziorem świata z powierzchnią wynoszącą ok. 370 tys .
km² ( zmieniającą się wskutek wahań poziomu wody , w 1930 roku wynosiła ona 442 tys .
km² ) .
Maksymalna głębokość to 1025 m. Zasolenie od słodkowodnej części północnej do 10–12‰ w części środkowej i południowej i do bardzo dużego , sięgającego nawet 300‰ w zamkniętej zatoce Kara-Bogaz-Goł .
Czas wymiany wód wynosi 250 lat .
W starożytności nosiło różne nazwy : `` Ocean Hyrkański '' , `` Morze Azarskie '' i `` Morze Kwalijskie '' .
W epoce antycznej i przez znaczną część średniowiecza powszechnie uważano , że Morze Kaspijskie stanowi zatokę wielkiego oceanu północnego ( pogląd taki głosili m.in .
Eratostenes , Strabon , Pomponiusz Mela , Izydor z Sewilli ) .
Herodot natomiast pisał : `` Morze Kaspijskie ( Κασπίη θάλασσα ) jest morzem osobnym ( ἑωυτῆς ) i nie łączy się z innym morzem .
Bo np .
całe morze , po którym jeżdżą Hellenowie , dalej morze leżące poza Słupami Heraklesa , tak zwane Atlantyckie , i Morze Czerwone – tworzą jedną całość .
Morze Kaspijskie zaś jest inne , samo dla siebie ; długość jego wynosi dla posługującego się wiosłem piętnaście dni jazdy , a szerokość , tam gdzie jest stosunkowo najszersze – osiem dni jazdy '' .
Morze Kaspijskie , tak jak Morze Śródziemne i Morze Czarne , jest reliktem Oceanu Tetydy ( zob .
Morze Sarmackie ) .
Ponieważ zasilane jest rzekami , poziom wody podnosił się i opadał znacząco wiele razy , a w suchych okresach wysychało odkładając na dnie pokłady soli , pokryte później osadami naniesionymi przez wiatr .
Niektórzy rosyjscy historycy utrzymują , że podniesienie się poziomu wody w morzu w średniowieczu spowodowało powódź na obszarach nadbrzeżnych .
W latach 40.–70 .
XX w. poziom morza opadał , potem podniósł się do poziomów z lat 30 .
Obecnie poziom jest raczej stabilny z tendencją do opadania .
