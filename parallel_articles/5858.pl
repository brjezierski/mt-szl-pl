W Morzu Kaspijskim żyją jesiotry , których ikra ceniona jest na całym świecie ze względu na walory smakowe – jest to jeden z lepszych rodzajów kawioru .
Populacji jesiotrów grozi wyginięcie z powodu przełowienia , więc organizacje ekologiczne starają się o zakaz połowów tych ryb do czasu , aż ich populacja powróci do równowagi .
Region Morza Kaspijskiego jest ważny dla światowej gospodarki ze względu na znajdujące się na jego dnie duże złoża ropy .
Stało się to przyczyną sporu o podział wód między pięć nadbrzeżnych państw .
Do Morza Kaspijskiego wpadają rzeki Kura , Ural i Wołga .
Żegluga między Morzem Kaspijskim a Azowskim możliwa jest przez Kanał Wołga-Don , między Morzem Kaspijskim a Bałtykiem przez Kanał Wołżańsko-Bałtycki , a z Morzem Białym przez Kanał Białomorsko-Bałtycki .
