Język kaszkajski
Język kaszkajski – język turkijski , należący do grupy oguzyjskiej tychże , używany przez Kaszkajów , zamieszkujących głównie południowoirański ostan Fars .
Dzieli wiele podobieństw z językami azerskim i afszarskim , wraz z którym bywa czasem klasyfikowany jako dialekt tego pierwszego .
Zapisywany perską wersją pisma arabskiego .
