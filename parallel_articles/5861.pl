Kaszubistyka
Kaszubistyka – dyscyplina filologiczna , zwyczajowo ujmowana jako dział slawistyki , zajmująca się badaniem języka , literatury , kultury i dziejów Kaszubów .
Ważnym ośrodkiem dla rozwoju kaszubistyki jest Uniwersytet Gdański , były prowadzone studia o kierunku polonistyka ze specjalnością kaszubistyczną .
Rok akademicki 2012/2013 był ostatnim , kiedy te studia .
Zostały one zastąpione przez nowy odrębny kierunek etnofilologia kaszubska .
