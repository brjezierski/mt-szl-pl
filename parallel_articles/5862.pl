Jaskier kaszubski
Jaskier kaszubski ( `` Ranunculus cassubicus '' ) – bardzo zmienny gatunek rośliny ( uznawany też za grupę drobnych gatunków ) należący do rodziny jaskrowatych .
Jego zasięg obejmuje Europę Środkową i Wschodnią .
Występuje w żyznych lasach liściastych i jest rośliną trującą .
Rozmieszczenie geograficzne .
Gatunek występuje w Europie Środkowej od wschodniej części Alp , poprzez Austrię , Czechy , Słowację , cały łuk Karpat i dalej na wschód , sięgając poprzez środkową Ukrainę po Ural .
Na północy jaskier ten rośnie w południowej Finlandii , na nielicznych stanowiskach także w środkowej i południowej Szwecji .
W Polsce gatunek rozpowszechniony jest w południowo-wschodniej części kraju , dość liczne stanowiska posiada na Śląsku , Kaszubach i Warmii .
Bardzo rozproszone stanowiska ma w Polsce Środkowej , brak go zupełnie w północno-zachodniej części kraju .
