Literatura kaszubska
Literatura kaszubska – literatura piękna tworzona w języku kaszubskim lub o tematyce kaszubskiej .
Literatura kaszubskojęzyczna .
Do XIX wieku .
Pierwszymi tekstami kaszubsko/pomorskojęzycznymi były różnego rodzaju modlitewki , zaklinania oraz formułki zawierające w sobie pozostałości wierzeń Pomorzan .
Za najstarsze dzieła spisane po kaszubsku uważa się `` Dutki brzeskie '' zapisane w 1402 .
