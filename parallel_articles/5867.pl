Kaszubskie nuty
Kaszubskie nuty ( kasz .
Kaszëbsczé nótë , inne nazwy : Alfabet kaszubski , Abecadło kaszubskie ) – tradycyjna pieśń kaszubska , będąca jednym z najbardziej rozpoznawalnych elementów folkloru kaszubskiego .
Pieśń jest wyliczanką , a nieodzowna do jej wykonania jest plansza z rysunkami – równolegle do śpiewanego tekstu jedna osoba wskazuje odpowiedni obrazek na pięciolinii ( stąd jedna z używanych nazw : kaszubskie nuty ) .
Śpiewane są kolejno trzy pięciolinie , z tym , że po dojściu do końca każdej z nich powraca się w odwrotnej kolejności do początku ( w ten sposób z każdą kolejną zwrotką tekst piosenki wydłuża się ) .
Pieśń śpiewana jest wolno , z pewnym namaszczeniem , choć w pierwotnej wersji miała szybkie tempo .
Pochodzenie pieśni .
Z drobnymi różnicami w melodii i tekście , pieśń znana jest na całych Kaszubach .
Była przede wszystkim śpiewana na weselach , ale towarzyszyła też innym uroczystościom rodzinnym .
Prawdopodobnie z powodu silnego wrośnięcia w tradycję rodzinną przyjęło się uważać , że w okresie zaborów odgrywała dużą rolę w zachowaniu języka kaszubskiego .
Przeczy temu Izydor Gulgowski , który tak opisał „ kaszubskie hieroglify ” :
`` przy okazji [ wesela ] widziałem kaszubskie hieroglify .
Na drzwiach rysowano kredą znaki i objaśniano je śpiewanym tekstem .
Sołtys Gołunia Jakub Langowski opowiedział mi , że jakieś 35 lat temu [ czyli w latach 1870 . ]
poznał tę zabawę w okolicy Żuław Wiślanych .
Pierwotny tekst był niemiecki , a jego brat Wilhelm przetłumaczył go na kaszubski '' .
Na niekaszubskie pochodzenie pieśni wskazuje wcześniejszy opis zabawy druciarzy słowackich zanotowany przez Oskara Kolberga w 1846 roku :
`` [ ... ] śpiewak w miarę jak przedmioty nią [ pieśnią ] objęte jedne po drugich wymienia , rysuje je zaraz kredą na stole , wybijając takt drugą ręką . ''
W XIX wieku zabawa znana też była na Polesiu , w Czechach , Morawach , Słowacji .
Tylko w najpopularniejszej odmianie pieśni z Kartuz występuje „ kaszëbskô stolëca ” i „ òznaczô Kaszëba ” .
W pozostałych tekstach , opartych na wersji niemieckiej , zamiast kaszubskiej stolicy jest „ cesarskô ” , a Kaszuba zastępuje szlachcic : „ to òznaczô szlachcëca ” .
