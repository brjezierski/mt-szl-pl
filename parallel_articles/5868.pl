Język kataloński
Język kataloński ( kat .
[ ] , [ ] ) – język romański z grupy zachodniej , mający status języka urzędowego w Andorze i hiszpańskich wspólnotach autonomicznych : Katalonii , Balearach i Walencji .
Jest zrozumiały dla 9 milionów ludzi , z których większość zamieszkuje wymienione regiony Hiszpanii .
Klasyfikacja i języki pokrewne .
Według `` Ethnologue '' kataloński jest językiem wschodnioiberyjskim .
Dzieli wiele cech z hiszpańskim , włoskim i francuskim .
Najbliższym krewnym katalońskiego jest język oksytański .
Z powodu tego podobieństwa oraz opierając się na pewnych teoriach historycznych dotyczących pochodzenia Katalończyków , niektórzy językoznawcy zaliczają kataloński nie do grupy iberoromańskiej , lecz galoromańskiej .
Jeszcze inni wyróżniają dla tych dwóch języków osobną grupę oksytańską .
Jak wszystkie języki romańskie , kataloński wywodzi się z łaciny .
Zasięg geograficzny .
W pierwszych czterech co najmniej 75 % ludności potrafi mówić po katalońsku , 90 % rozumie go , a około 50 % używa go na co dzień .
Znajomość i popularność katalońskiego w pozostałych jest nieco mniejsza .
Status oficjalny .
Język kataloński jest jedynym językiem urzędowym Andory , a także , obok hiszpańskiego , hiszpańskich wspólnot autonomicznych Katalonii , Walencji i Balearów .
Mimo dość licznej grupy mówiących , nie ma podobnego statusu w Aragonii , chociaż jest chroniony przez aragońskie prawo od 1990 r .
Ze względu na francuskie prawo i brak ratyfikacji Europejskiej Karty Języków Regionalnych lub Mniejszościowych , kataloński nie może uzyskać statusu urzędowego w Katalonii Północnej .
