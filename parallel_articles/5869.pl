Katalonia
Katalonia ( kat .
`` Catalunya '' [ ] , hiszp .
`` Cataluña '' , oksytański `` Catalonha '' ) – wspólnota autonomiczna Hiszpanii .
W szerszym znaczeniu – region geograficzny i kraina historyczna na Półwyspie Iberyjskim z ośrodkiem kulturalnym i administracyjnym w Barcelonie .
Hymnem Katalonii jest pieśń `` Els Segadors '' .
Świętem narodowym jest dzień 11 września – data zdobycia Barcelony przez wojska Burbonów w 1714 r. podczas wojny o sukcesję ( wydarzenie to stanowiło porażkę Katalończyków ) .
Barwami narodowymi Katalonii , podobnie jak innych regionów Hiszpanii i Francji położonych nad Morzem Śródziemnym , są złoty i czerwony .
Układają się one w 4 czerwone pasy ( `` quatre barres '' ) na złotym tle .
Popularnym symbolem Katalonii jest osioł – symbol mądrego i spokojnego , lecz upartego dążenia do celu ( stanowiąc przez to przeciwieństwo byka symbolizującego Hiszpanię ) .
Geografia .
Leży nad Morzem Śródziemnym .
Graniczy z Francją i Andorą oraz z Aragonią i Walencją .
Jest regionem wyżynno-górzystym , z nielicznymi nizinami na wybrzeżu i w dolinie rzeki Ebro .
Do historycznej Katalonii należy również większa część departamentu Pireneje Wschodnie we Francji ( tzw .
Katalonia Północna ) .
Na terenie tego departamentu leży również katalońska enklawa Llívia .
Na określenie wszystkich katalońskojęzycznych krajów dawnej Korony Aragonii używa się pojęcia kraje katalońskie ( `` Països Catalans '' ) .
Podział administracyjny .
Wspólnotę autonomiczną Katalonii tworzą cztery prowincje państwa hiszpańskiego :
Granice prowincji zostały ustalone odgórnie przez władze hiszpańskie w roku 1833 , bez uwzględnienia lokalnych uwarunkowań .
Z tego powodu funkcjonują wyłącznie jako jednostki podziału terytorialnego Hiszpanii , nie są natomiast statutowymi jednostkami podziału Katalonii .
Podstawowy podział terytorialny Katalonii , zarówno w stosunkach urzędowych , jak i w świadomości społecznej , opiera się na średniowiecznej jednostce administracyjnej – comarce ( `` comarca '' ) .
W mediach i życiu codziennym comarki są podstawowym wyznacznikiem lokalizacji czy miejsca zamieszkania .
W porównaniu z innymi wspólnotami Hiszpanii , kompetencje katalońskich comarek są szerokie .
Współczesny podział na comarki został zasadniczo ustalony w roku 1936 i nieco zmodyfikowany w latach 1988 i 1990 .
W pewnym stopniu ignoruje on istnienie prowincji – ich granice w kilku przypadkach przecinają granice comarek .
Obecnie liczba comarek wynosi 41 .
Obecny statut autonomii przewiduje dodatkowo przywrócenie średniowiecznych jednostek administracyjnych najwyższego rzędu zwanych `` vegueries '' – wikariatami .
Zastąpiłyby one w stosunkach wewnętrznych hiszpańskie prowincje .
Ich liczba i granice są nadal przedmiotem dyskusji .
W obecnej chwili różne instytucje katalońskie w swojej działalności stosują różną organizację terytorialną , przy czym z reguły nie pokrywa się ona z podziałem na prowincje .
Rząd kataloński działa poprzez tzw .
