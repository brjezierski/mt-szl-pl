Katerini
Katerini ( ) – miasto w Grecji , w administracji zdecentralizowanej Macedonia-Tracja , w regionie Macedonia Środkowa , w jednostce regionalnej Pieria .
Siedziba gminy Katerini .
W 2011 roku liczyło 55 997 mieszkańców .
Położone między Masywem Olimpu a Morzem Egejskim .
W pobliżu Katerini znajdują się znane kurorty , jak Leptokaria czy Paralia Katerinis , a także stanowiska archeologiczne ( Dion ) i poweneckie twierdze ( Platamonas ) .
