Katmandu
Katmandu ( nep .
काठमाडौं , trl .
`` Kāthmāndu '' ) – stolica Nepalu położona w środkowej części kraju , w Dolinie Katmandu .
Wraz z miastami Patan i Bhaktapur tworzy aglomerację liczącą powyżej miliona mieszkańców .
Miasto ograniczają rzeki – od zachodu Bishumati , a od południa i wschodu Bagmati .
Katmandu zostało założone w 723 roku przez króla Gunakamadevę .
Miasto jest głównym ośrodkiem przemysłowym w kraju , najważniejsze to przemysł włókienniczy , spożywczy , drzewny , materiałów budowlanych i obuwniczy .
Międzynarodowy port lotniczy Tribhuvan International Airport .
Uniwersytet ( Tribhuvan University of Nepal ) zlokalizowany jest w Kirtipur .
Połączenie drogowe z Birgańdź ( na granicy z Indiami ) i Kodari ( przy granicy z Chinami ) ; koleją linową z Hetauri .
Stare miasto znane jest z hinduistycznych i buddyjskich świątyń .
Do najważniejszych zabytków należą Katmandu Durbar Square ( m.in .
pałac królewski Hanuman Doka ) , Patarn Durbar Square , Bhaktapur oraz stupy Swayambhu i Bodnath .
Większość turystów zamieszkuje w dawnej handlowej dzielnicy Thamel , pełnej hoteli w różnym standardzie , restauracji , sklepów z pamiątkami oraz ekwipunkiem turystycznym .
