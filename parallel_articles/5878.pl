Katyń ( przystanek kolejowy )
Katyń ( ros .
`` Катынь '' ) – przystanek kolejowy i posterunek odstępowy w dieriewni Stacja Katyń , 3 km od miejscowości Katyń , w rejonie smoleńskim , w obwodzie smoleńskim , w Rosji .
Leży na linii Moskwa - Mińsk - Brześć - Warszawa .
Historia .
Stacja kolejowa Katyń powstała w XIX w. na drodze żelaznej moskiewsko-brzeskiej pomiędzy stacjami Smoleńsk a Gusino .
Później zdegradowana do przystanku kolejowego i posterunku odstępowego .
