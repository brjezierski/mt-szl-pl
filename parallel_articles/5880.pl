Pieprz metystynowy
Pieprz metystynowy ( `` Piper methysticum '' ) znany też jako `` kava '' – gatunek roślin z rodziny pieprzowatych .
Pochodzi z wysp Mikronezji i Vanuatu .
Roślina o długiej tradycji uprawy na wyspach zachodniego Pacyfiku .
Inne nazwy lokalne : ʻawa ( Hawaiʻi ) , ʻava ( Samoa ) , yaqona ( Fidżi ) i sakau ( Pohnpei ) .
Termin „ kava ” odnosi się także do naparu sporządzonego z tej rośliny .
Zastosowanie .
Pieprz metystynowy od dawna ma znaczenie religijne , polityczne i kulturowe na obszarze Pacyfiku .
Używany jest od tysięcy lat w celach leczniczych , relaksacyjnych i kulturowych .
Kultura spożywania .
W tradycyjnej postaci napój zaparzany z pieprzu metystynowego spożywany jest na większości wysp południowego Pacyfiku .
Stopień sformalizowania rytuału picia kavy zależy od danej tradycji i kultury , niemniej niemal na całym Pacyfiku jest ona napojem spożywanym jako absolutnie niezbędny dodatek do wszelkiego rodzaju spotkań , wydarzeń towarzyskich , politycznych i religijnych .
Napój ten jest ważnym symbolem tożsamości narodów Pacyfiku , stąd jej wizerunki można znaleźć min .
na monetach , flagach i banknotach .
Jednocześnie istnieją spore różnice pomiędzy poszczególnymi kulturami spożywania naparu .
