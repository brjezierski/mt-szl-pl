Kavaja
Kavaja ( alb .
forma nieokreślona : `` Kavajë '' ) – miasto w zachodniej Albanii , stolica okręgu Kavaja .
Według danych z 2003 r. miasto liczy 28 200 mieszkańców ; pod względem wielkości jest dziesiątym miastem w Albanii .
95 % mieszkańców miasta to muzułmanie .
W latach 1990-1991 miasto było centrum protestów antykomunistycznych .
W okresie zamieszek w 1997 r. Kavaja pozostała jednym z najspokojniejszych miast w całej Albanii , a to dzięki cywilnym patrolom , zorganizowanym przez władze religijne .
W tym mieście urodził się piłkarze : Skerdilaid Curri , Andi Lila oraz Altin Lala .
W mieście znajduje się siedziba klubu Besa Kavajë , grającego w najwyższej klasie rozgrywek ligowych w Albanii .
