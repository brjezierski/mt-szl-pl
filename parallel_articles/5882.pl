Kawaguchi
– miasto w Japonii ( Honsiu ) , w prefekturze Saitama .
Ma powierzchnię 61,95 km2 .
W 2020 r. mieszkało w nim 594 461 osób , w 265 961 gospodarstwach domowych .
W 2010 r. miało 500 311 mieszkańców w 210 179 gospodarstwach domowych ) , a wraz z miastem Hatogaya ( włączonym do miasta Kawaguchi 11-10-2011 ) 561 211 mieszkańców , w 234 791 gospodarstwach domowych .
W mieście rozwinął się przemysł maszynowy , metalowy , elektrotechniczny , włókienniczy , chemiczny oraz hutniczy .
