Kawno ( powiat choszczeński )
Kawno ( niem .
`` Hasselbruch '' ) – kolonia w Polsce położona w województwie zachodniopomorskim , w powiecie choszczeńskim , w gminie Bierzwnik .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gorzowskiego .
W roku 2007 kolonia liczyła 18 mieszkańców .
Miejscowość wchodzi w skład sołectwa Breń .
Geografia .
Kolonia leży ok. 1,5 km na północ od Brenia , 300 m na południe od jeziora Przytoczno .
