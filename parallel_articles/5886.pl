W latach 1940–1944 przechowywano w tym mieście część polskiego złota wywiezionego podczas II wojny światowej , aby uniknąć jego przejęcia przez hitlerowskie Niemcy .
W mieście rozwinął się przemysł spożywczy oraz skórzany .
