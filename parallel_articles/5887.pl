Kaysersberg-Vignoble
Kaysersberg-Vignoble – gmina we Francji , w regionie Grand Est , w departamencie Górny Ren .
W 2013 roku populacja ludności wynosiła 4634 mieszkańców .
Gmina została utworzona 1 stycznia 2016 roku z połączenia trzech ówczesnych gmin : Kaysersberg , Kientzheim oraz Sigolsheim .
Siedzibą gminy została miejscowość Kaysersberg .
