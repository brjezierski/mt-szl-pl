Ogólne położenie .
Zdecydowana większość terytorium Kazachstanu leży w Azji , tylko stosunkowo niewielka część północno-zachodniego Kazachstanu leży w Europie .
Przeważająca większość terytorium kraju należy do Azji Środkowej , której Kazachstan zajmuje największą część .
Do Kazachstanu należą także południowe obrzeża zachodniej Syberii .
