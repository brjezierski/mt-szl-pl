`` Kazachstan '' , `` Riespublika Kazachstan '' ) – państwo leżące częściowo w Azji ( 88 % powierzchni ) i częściowo w Europie ( 12 % powierzchni – tereny na zachód od rzeki Emba ) , powstałe w 1991 w wyniku rozpadu Związku Radzieckiego , graniczące z Chinami ( 1460 km granicy ) , Kirgistanem ( 980 km ) , Turkmenistanem ( 380 km ) , Uzbekistanem ( 2300 km ) oraz Federacją Rosyjską ( 6467 km ) .
Łączna długość granic Kazachstanu wynosi 12 187 km .
Kazachstan ma również dostęp do największego jeziora świata – Morza Kaspijskiego – na długości 2340 km – jest przy tym największym państwem śródlądowym na świecie pod względem powierzchni .
Całkowita powierzchnia kraju wynosi 2 724 900 km² i jest największym w całości państwem Azji Środkowej .
