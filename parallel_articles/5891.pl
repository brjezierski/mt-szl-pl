Kazań
Kazań ( , , ) – miasto w Rosji , stolica Tatarstanu , leżące przy ujściu Kazanki do Wołgi ( Samarski Zbiornik Wodny ) .
Liczy 1,25 mln mieszkańców ( 2021 ) .
Ośrodek przemysłu maszynowego , chemicznego ( farmacja ) , gumowego , futrzarskiego , skórzano-obuwniczego , włókienniczego i spożywczego .
Jedno z większych rosyjskich centrów produkcji lotniczej , w mieście znajdują się dwie duże wytwórnie : Kazańskie Zjednoczenie Przemysłu Lotniczego oraz Kazańska Fabryka Śmigłowców .
Ważny ośrodek naukowy : 11 uczelni , w tym od 1804 uniwersytet ( założony jako trzeci w Rosji ) , filia Rosyjskiej Akademii Nauk oraz liczne instytuty naukowe .
Historia .
Założony w drugiej połowie XI wieku przez Bułgarów wołżańsko-kamskich .
Następnie w Złotej Ordzie .
Po jej rozpadzie – w latach 1438–1552 – był stolicą Chanatu Kazańskiego i ważnym ośrodkiem handlowym Powołża ( na szlaku Europa-Azja ) .
W 1487 zdobyty przez wojska moskiewskie .
W 1552 , Chanat Kazański został podbity i zlikwidowany przez Iwana IV Groźnego , a Kazań przyłączony do Rosji .
Od 1708 stolica guberni , ośrodek budowy statków , rozwinięte sukiennictwo ( pierwsza manufaktura w 1714 ) .
Po 1768 Kazań był jednym z miejsc zsyłek konfederatów barskich .
Wśród zesłanych tu znajdowali się Maurycy Beniowski , Antoni Pułaski , Franciszek Ignacy Narocki , Piotr Potocki i Michał Czarnocki .
