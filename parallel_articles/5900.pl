Kałkowskie
Kałkowskie – wieś w Polsce położona w województwie wielkopolskim , w powiecie ostrowskim , w gminie Sośnie .
Leżą ok. 23 km na południe od Ostrowa Wielkopolskiego , przy drodze Sośnie-Chojnik .
Przed 1932 rokiem miejscowość położona była w powiecie odolanowskim , w latach 1975-1998 w województwie kaliskim , w latach 1932-1975 i od 1999 w powiecie ostrowskim .
