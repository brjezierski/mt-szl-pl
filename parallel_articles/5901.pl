W 1924 roku w ZSRR pismo ojracko-kałmuckie zostało zastąpione przez cyrylicę , którą w 1930 roku zastąpiła łacinka , którą jeszcze raz zmieniła cyrylica o wymowie mongolskiej w 1938 roku .
Kałmucy ( Ojraci ) w Zachodnich Chinach nadal posługują się pismem ojracko-kałmuckim .
W Urumczi wydają kilka gazet i pism w języku ojrackim , nadawane są w tym języku także programy radiowe i telewizyjne .
Język ojracki w Mongolii nie uzyskał statusu oficjalnego i jest używany tylko w życiu codziennym , poza tym wypiera go język chałcha-mongolski .
