Ukształtowanie powierzchni .
Zdecydowana większość powierzchni kraju ma charakter równinny , jedynie miejscami krajobraz jest łagodnie pofałdowany .
Większość terytorium kraju leży na Nizinie Nadkaspijskiej , której część stanowi położona poniżej poziomu morza depresja .
Strefa czasowa .
Od 2014 Kałmucja leży w strefie czasowej czasu moskiewskiego ( MSK ) .
UTC +3:00 przez cały rok .
Wcześniej , przed 27 marca 2011 roku , obowiązywał czas standardowy ( zimowy ) strefy UTC+3:00 , a czas letni – UTC+4:00 .
Hydrografia .
Z powodu suchego klimatu rzek na terenie kraju jest mało i są zwykle niewielkie ; niektóre z nich napełniają się wodą tylko w okresie opadów .
W północno-wschodniej części regionu płynie Wołga – najdłuższa rzeka Europy , jednak dostęp do niej Kałmucja ma bardzo ograniczony , bo jedynie na 12-kilometrowym odcinku .
Innymi większymi rzekami są Kuma i Wschodni Manycz .
Kałmucja ma dostęp do Morza Kaspijskiego ; strefa wód terytorialnych obejmuje ok. 1,4 tys .
km² .
Poza tym jezior jest niewiele , największe z nich to Manycz-Gudiło , Jezioro Sarpińskie i zespół Jezior Sostyńskich .
Zasoby naturalne .
Głównymi zasobami naturalnymi Kałmucji są węgiel kamienny , ropa naftowa i gaz ziemny .
