osób .
Nazwa .
Nazwa Kałmuków wywodzi się od tureckiej nazwy „ Kalmak ” , używanej przez zislamizowanych Turków Azji Środkowej w stosunku do Mongołów ( w sensie ludów kontynuujących tradycje Imperium mongolskiego ) nie nawróconych na islam .
Zgodnie z najprawdopodobniej ludową etymologią „ Kalmak ” miało oznaczać „ pozostawać ” i odróżniać „ pozostających w pogaństwie ” od nawróconych na islam .
Ponieważ spośród ludów mongolskich najbliższym sąsiadem muzułmańskich Turków byli Ojraci , w praktyce nazwa „ Kalmak ” najczęściej oznaczała tych ostatnich .
Kiedy Ojraci w XVII wieku przybyli nad dolną Wołgę Rosjanie przejęli ich turecką nazwę , która w ich języku przybrała formę „ Kałmyki ” .
Pod koniec XIX wieku rosyjscy Ojraci ostatecznie zaakceptowali nazwę „ Kałmyki ” jako swoje samookreślenie , przy czym w ich języku przyjęła ona formę „ Hal'mgud ” albo „ Hal'mg ” .
