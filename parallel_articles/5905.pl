Kałusz
Kałusz ( ukr .
Калуш ) – miasto na Ukrainie , w obwodzie iwanofrankiwskim , siedziba rejonu kałuskiego .
Miasto królewskie lokowane w 1534 roku położone było w XVI wieku w województwie ruskim .
Następnie znajdowało się w zaborze austriackim ( Galicja ) od 1772 do 1918 roku .
Powróciło do Polski po upadku Austro-Węgier , na krótko znajdując się pod administracją ZUNR ( listopad 1918 – marzec 1919 ) , od 1939 roku pod okupacją radziecką , 1941 – niemiecką , 1945 – na powrót radziecką , od 1991 – pod administracją ukraińską .
Znajduje tu się stacja kolejowa Kałusz , położona na linii Stryj – Iwano-Frankiwsk ( odcinek dawnej Galicyjskiej Kolei Transwersalnej ) .
Historia .
W roku 1469 król Kazimierz Jagiellończyk ufundował w leżącej w dobrach królewskich wsi Kałusz katolicki kościół parafialny .
