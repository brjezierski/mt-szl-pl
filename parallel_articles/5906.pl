Kałuszyn ( gmina w guberni warszawskiej )
Kałuszyn – dawna gmina wiejska istniejąca w XIX wieku w guberni warszawskiej .
Nazwa gminy pochodzi od wsi Barcząca , lecz siedzibą władz gminy było miasto Kałuszyn , który stanowił odrębną jednostkę administracyjną .
Za Królestwa Polskiego gmina Kałuszyn należała do powiatu mińskiego w guberni warszawskiej .
Gminę zniesiono w połowie 1870 roku a jej obszar włączono do gmin Chrościce i Kuflew .
