Kańczuga
Kańczuga – miasto w województwie podkarpackim , w powiecie przeworskim , siedziba gminy miejsko-wiejskiej Kańczuga , położone na Podgórzu Rzeszowskim .
Leży nad rzeką Mleczką .
Leży w historycznej ziemi przemyskiej .
Prywatne miasto szlacheckie lokowane w 1441 roku położone było w XVI wieku w województwie ruskim , w 1739 roku należało wraz z folwarkiem do klucza Kańczuga Lubomirskich .
W latach 1975–1998 miasto administracyjnie należało do województwa przemyskiego .
