Kaziopole
Kaziopole – wieś w Polsce położona w województwie wielkopolskim , w powiecie obornickim , w gminie Rogoźno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa pilskiego .
Kaziopole jest wsią typowo rolniczą .
Zabudowa wsi zwarta , skoncentrowana wzdłuż jednej drogi , jest to więc ulicówka .
