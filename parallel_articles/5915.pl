Kecskemét
Kecskemét ( ) – miasto na Węgrzech , położone na Wielkiej Nizinie Węgierskiej między Budapesztem a Segedynem .
Stolica komitatu Bács-Kiskun .
Jego nazwa pochodzi od słowa `` kecske '' oznaczającego „ kozę ” i `` met '' oznaczającego „ dzielnicę ” .
Miasto liczy 113,3 tys .
mieszkańców ( 2011 r. ) i jest pod tym względem 8. miastem Węgier .
Pomimo suchego klimatu kontynentalnego , miasto jest stolicą regionu rolniczego , znanego z sadownictwa oraz produkcji owocowej wódki pálinka .
Historia .
Pierwsze ślady osadnictwa człowieka na terenach okolic Kecskemét pochodzą sprzed 5000 lat .
W czasie pierwszej inwazji mongolskiej w 1241 r. miasto zostało zniszczone , wkrótce zostało jednak odbudowane .
W średniowieczu Kecskemét rozwijał się głównie jako ośrodek handlowy , prawa miejskie uzyskał w roku 1368 .
W 1950 r. miasto zostało stolicą regionu Bács-Kiskun .
Przemysł .
18 czerwca 2008 niemiecki koncern Daimler AG ogłosił , iż kosztem 800 milionów euro zamierza wybudować w Kecskemét fabrykę samochodów marki Mercedes-Benz , która będzie zatrudniać około 2500 pracowników .
Fabryka została otwarta w marcu 2012 .
Zdaniem Ferenca Gyurcsánya , premiera Węgier , była to największa pojedyncza inwestycja w historii Węgier .
