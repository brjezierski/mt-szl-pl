Kefar Sawa
Kefar Sawa ( hebr .
כפר סבא ; arab .
كفار سابا ) – miasto położone w Dystrykcie Centralnym w Izraelu .
Leży na równinie Szaron w aglomeracji miejskiej Gusz Dan , w otoczeniu miast Ra ’ ananna i Hod ha-Szaron , moszawów Gan Chajjim , Sede Warburg , Cofit , Newe Jamin , Eliszama , Kefar Malal i Ramot ha-Szawim , kibucu Nir Elijjahu , oraz wiosek Bet Berl i Sede Chemed .
W odległości 5 km na wschód od miasta znajduje się terytorium Autonomii Palestyńskiej i arabskie miasto Kalkilja .
Historia .
Na północnych peryferiach miasta znajduje się grobowiec , który powszechnie uznaje się za miejsce pochowania Beniamina , syna Jakuba .
Miejsce to jest czczone zarówno przez Muzułmanów , jak i przez Żydów .
W starożytności znajdowało się tutaj miasto Capharsaba należące do izraelskiego plemienia Efraima .
Wspomina o nim żydowski historyk Józef Flawiusz , gdy opisuje wysiłki Aleksandra Jannaja zatrzymania wojsk syryjskich ( `` Antiquities '' , tom 13 , rozdz .
15 ) .
