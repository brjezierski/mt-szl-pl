Keila ( wodospad )
Keila ( est .
`` Keila juga '' ) – wodospad w północnej Estonii , na rzece Keila w pobliżu jej ujścia do zatoki Lohusalu .
Położony jest w miejscowości Keila-Joa , w prowincji Harjumaa .
Wodospad ma 6 m wysokości i około 60-70 m szerokości .
Jest trzecim pod względem wielkości wodospadem w Estonii po wodospadach Narwa i Jägala .
Przy wodospadzie działa niewielka hydroelektrownia o mocy 365 kW , która została uruchomiona w 1928 roku .
Zaprzestała działalności w latach 90 .
XX wieku .
Została odnowiona i ponownie uruchomiona w 2006 roku .
W sąsiedztwie wodospadu znajduje się dwór w stylu neogotyckim powstały w latach 30 .
XIX wieku według projektu Andreia Stackenschneidera .
W pobliżu wodospadu przebiega również trzykilometrowa ścieżka przyrodnicza w parku Keila-Joa .
