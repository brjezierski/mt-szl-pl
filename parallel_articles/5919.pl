Kielce
Kielce – miasto na prawach powiatu w południowo-wschodniej Polsce , stolica województwa świętokrzyskiego .
Położone w Górach Świętokrzyskich , nad rzeką Silnicą , historycznie w Małopolsce .
Centralny ośrodek aglomeracji kieleckiej , stanowi regionalne centrum gospodarcze , naukowe , kulturalne oraz wystawienniczo-targowe .
Według danych GUS z 31 grudnia 2021 roku , w Kielcach mieszkało 185 478 osób .
Kielce uzyskały lokację miejską przed 1259 rokiem .
W ostatniej ćwierci XVI wieku położone były w powiecie chęcińskim województwa sandomierskiego , były własnością biskupstwa krakowskiego .
Położenie .
Kielce położone są w Górach Świętokrzyskich .
W obrębie miasta ulokowane są pasma Kadzielniańskie i Dymińskie .
Kielce przecina niewielka rzeka Silnica , będąca lewostronnym dopływem Bobrzy .
Na terenie miasta znajduje się szereg rezerwatów przyrody ożywionej i nieożywionej , m.in .
Kadzielnia , Karczówka , Ślichowice , Wietrznia , Biesak-Białogon .
Ponadto granice administracyjne miasta obejmują sporą część Chęcińsko-Kieleckiego Parku Krajobrazowego .
Historycznie i kulturowo Kielce leżą w północnej Małopolsce , w ziemi sandomierskiej .
Charakterystyczną mową tego regionu jest tzw .
gwara kielecka , którą posługują się mieszkańcy południowej i środkowej części regionu .
Miasto charakteryzuje się znaczną różnicą poziomów – od 260 do 408 m n.p.m .
( najwyższe wzniesienie : Telegraf , najniżej położone miejsce – dolina Silnicy ) .
