Kemerowo
Kemerowo ( ros .
Кемерово ) , do 1932 Szczegłowsk – miasto w azjatyckiej części Rosji , centrum administracyjne obwodu kemerowskiego , siedziba prawosławnej metropolii kuzbaskiej .
Leży nad rzeką Tom , przy ujściu rzeki Iskitimka .
Powstało w 1918 z połączenia wsi Szczegłowo i Kemerowo .
Prawa miejskie otrzymało w 1925 .
W 2010 Kemerowo liczyło 532 981 mieszkańców ( w 2007 było ich 557 tysięcy ) .
W 2006 w całej aglomeracji mieszkało około 580 tysięcy osób .
W ZSRR był to rejon zesłań i obóz pracy , między innymi dla Polaków – Kemerowożyłstroj i ITŁ .
Obecnie jest to duży ośrodek Kuźnieckiego Zagłębia Węglowego – z rozwiniętym przemysłem wydobywczym węgla kamiennego , maszynowym , chemicznym , koksochemicznym , materiałów budowlanych , włókienniczym – a także ośrodek naukowy ( pięć szkół wyższych ) i kulturalny .
W Kemerowie znajduje się port rzeczny i port lotniczy .
Wewnętrzny transport publiczny zapewniają sieci : tramwajowa , trolejbusowa i autobusowa .
W mieście funkcjonują kluby piłkarskie Dinamo Kemerowo i Kuzbass Kemerowo .
