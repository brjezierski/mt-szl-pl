Ken Sugimori
– japoński projektant gier wideo i ilustrator .
Najbardziej znany jako projektant postaci i dyrektor artystyczny gier z serii Pokémon .
Osobiście stworzył pierwsze 151 Pokémonów .
Sugimori pracował też nad częścią filmów Pokémon , kartami kolekcjonerskimi i innymi grami , takimi jak seria Super Smash Bros .
Od początku 1981 do 1986 , Sugimori ilustrował fanzin o grach `` Game Freak '' , którego twórcą był Satoshi Tajiri .
Sugimori odkrył ten magazyn w sklepie z dōjinshi i postanowił się zaangażować .
Ostatecznie obaj zamienili Game Freak w firmę produkującą gry , której pierwszym dziełem było Mendel Palace .
