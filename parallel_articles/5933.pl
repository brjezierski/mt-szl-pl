Kenner
Kenner – miasto ( `` city '' ) w parafii Jefferson , w południowo-wschodniej części stanu Luizjana , w Stanach Zjednoczonych , położone pomiędzy jeziorem Pontchartrain na północy a rzeką Missisipi na południu , w aglomeracji Nowego Orleanu .
W 2013 roku miasto liczyło 66 975 mieszkańców .
Nazwa miasta pochodzi od plantatora i prawnika , Duncana F. Kennera .
W Kenner zlokalizowany jest port lotniczy Louis Armstrong New Orleans International Airport .
