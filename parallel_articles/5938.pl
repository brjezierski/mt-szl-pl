Kentucky
Kentucky ( ) , oficjalnie Commonwealth of Kentucky – stan na środkowym wschodzie Stanów Zjednoczonych .
Jest jednym z czterech stanów posługujących się w oficjalnym nazewnictwie terminem `` commonwealth '' ( pozostałe to : Massachusetts , Pensylwania i Wirginia ) .
Początkowo był częścią stanu Wirginia , ale został wydzielony z niego w 1792 roku , stając się 15. stanem dołączonym do Unii .
Kentucky graniczy na północy ze stanami : Illinois , Indiana i Ohio , na południu – z Tennessee , na wschodzie – z Wirginią Zachodnią i Wirginią , a na zachodzie – z Missouri .
Zajmując obszar 104 749 km² , stanowi 37. pod względem powierzchni stan Stanów Zjednoczonych .
W spisie ludności z 2020 roku populacja stanu wynosiła 4 505 836 mieszkańców .
Zachodnia część stanu położona jest na nizinie Missisipi .
Część środkowa i obszary północno-wschodnie są pagórkowate
( wyżyna Cumberland ) .
Historia .
10 grudnia 2021 r. o porze nocnej przez zachodnie Kentucky przeszło gwałtowne tornado , powodując poważne lub katastrofalne zniszczenia w wielu miastach , w tym : Mayfield , Benton , Dawson Springs i Bremen .
Potwierdzono co najmniej 58 ofiar śmiertelnych .
Geografia .
Około połowę powierzchni stanu stanowią grunty leśne , a do głównych gatunków drzew należą : buk , hikora , dąb czerwony , klon cukrowy , orzech , dąb biały i topola żółta .
Znajduje się tutaj najdłuższa jaskinia na świecie – Jaskinia Mamucia , która w 1941 roku została uczyniona parkiem narodowym .
Jest w dwóch strefach czasowych : wschodnia połowa stanu w a zachodnia połowa w .
Demografia .
Spis ludności z roku 2020 stwierdza , że stan Kentucky liczy 4 505 836 mieszkańców , co oznacza wzrost o 166 469 ( 3,8 % ) w porównaniu z poprzednim spisem z roku 2010 .
Dzieci poniżej piątego roku życia stanowią 6,1 % populacji , 22,4 % mieszkańców nie ukończyło jeszcze osiemnastego roku życia , a 16,8 % to osoby mające 65 i więcej lat .
50,7 % ludności stanu stanowią kobiety .
