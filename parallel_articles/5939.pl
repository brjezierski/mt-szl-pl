Kermanszah ( ostan )
Kermanszah ( dawniej Bachtaran ) – ostan w zachodnim Iranie przy granicy z Irakiem , w irańskiej części Kurdystanu .
Stolicą jest Kermanszah .
Klimat tego ostanu to podzwrotnikowy kontynentalny , suchy .
Głównymi rzekami Kermanszahu są : Ghare , Su oraz Rāvand .
Naturalna szata roślinna w tym ostanie tworzy różne zbiorowiska górskich stepów , a w wyższych partiach gór suche lasy oraz zarośla .
Podstawę gospodarki stanowi rolnictwo , które jest częściowo oparte na sztucznym nawadnianiu .
W tym regionie rozwinął się przemysł włókienniczy , chemiczny oraz spożywczy .
