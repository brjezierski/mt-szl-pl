Kermit ( Teksas )
Kermit – miasto w Stanach Zjednoczonych , w stanie Teksas , siedziba hrabstwa Winkler .
Nazwane na cześć Kermita Roosevelt , syna prezydenta USA Theodore ' a Roosevelta .
Demografia .
Według przeprowadzonego przez United States Census Bureau spisu powszechnego w 2010 roku miasto liczyło 5 708 mieszkańców .
Natomiast skład etniczny w mieście wyglądał następująco : Biali 70,8 % , Afroamerykanie 2,5 % , Azjaci 0,3 % , pozostali 26,4 % .
