Kietrz ( stacja towarowa )
Kietrz – zamknięta stacja techniczna położona w Kietrzu , w województwie opolskim .
Stacja nie jest eksploatowana w ruchu pasażerskim .
Historia .
Stacja Kiekrz została oddana do użytku w 1896 roku wraz z otwarciem linii kolejowej nr 194 : Pietrowice Wielkie – Kietrz .
Do 1945 roku stacja nosiła nazwę Katscher .
Ruch pasażerski na stacji wstrzymano w 1993 roku , a posterunek jest obecnie zamknięty .
Budynek stacyjny został zamieniony na lokal mieszkalny ( centralna część ) i biurowy ( niższa część – na lewo od środkowej części budynku , patrząc od strony peronów ) .
Część magazynowa została zburzona wiosną 2008 roku ( najprawdopodobniej bezprawnie – tzn .
bez zgody i wiedzy konserwatora zabytków ) .
