Keuruu
Keuruu ( szw .
`` Keuru '' ) – miasto w Finlandii , w prowincji Finlandia Zachodnia , w regionie Finlandia Środkowa .
Zajmuje powierzchnię 1430,8 km² a zamieszkuje je 10 706 osób ( stan na dzień 31 lipca 2010 ) .
Zostało założone w 1652 roku .
W mieście działa klub sportowy Keuruun Pallo , w tym sekcja hokeja na lodzie pod nazwą KeuPa HT .
