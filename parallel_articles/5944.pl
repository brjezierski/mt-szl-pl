Kevelaer
Kevelaer – miasto w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf , w powiecie Kleve .
Liczy 28 328 mieszkańców ( 2010 ) .
Kevelaer leży ok. 6 km od granicy z Holandią .
Znane jest z Sanktuarium Matki Bożej Pocieszycielki Strapionych , najbardziej znanego i uczęszczanego w Niemczech .
Jest ono odwiedzane przez 800 000 pielgrzymów ( głównie z Niemiec i Holandii ) rocznie .
