Kevin De Bruyne
Kevin De Bruyne ( wym .
[ ] , ur .
28 czerwca 1991 w Drongen ) – belgijski piłkarz , występujący na pozycji pomocnika w angielskim klubie Manchester City .
Uczestnik Mistrzostw Świata 2014 , 2018 i 2022 oraz Mistrzostw Europy 2016 i 2020 .
De Bruyne rozpoczął swoją karierę w Genk , gdzie był podstawowym graczem , gdy wygrali ligę belgijską .
W 2012 dołączył do Chelsea , gdzie nie był często wykorzystywany , a następnie wypożyczony do Werderu Brema .
W 2014 podpisał kontrakt z Wolfsburgiem za 18 milionów funtów .
Stał się tam jednym z najlepszych graczy Bundesligi i to , że klub wygrał Puchar Niemiec w 2015 to w dużej mierze jego zasługa .
Latem 2015 roku De Bruyne dołączył do Manchesteru City za rekordowe 54 miliony funtów .
Od tego czasu zdobył cztery tytuły Premier League , pięć Pucharów Ligi i Puchar Anglii .
W sezonie 2017/2018 odegrał znaczącą rolę w ustanowieniu rekordu Manchesteru City , który stał się jedyną drużyną Premier League , która zdobyła 100 punktów w jednym sezonie .
W sezonie 2019/2020 De Bruyne ustanowił rekord największej liczby asyst w sezonie i został wybrany najlepszym piłkarzem Premier League .
De Bruyne zadebiutował na arenie międzynarodowej w 2010 .
Był członkiem reprezentacji Belgii , kiedy dotarła do ćwierćfinałów zarówno na Mistrzostwach Świata 2014 jak i na Mistrzostwach Europy 2016 i 2020 .
Reprezentował Belgię również gdy ta wygrała mecz o 3. miejsce z Anglią na Mistrzostwach Świata 2018 i został powołany do FIFA World Cup Dream Team .
De Bruyne został czterokrotnie wybrany do składu sezonu Ligi Mistrzów UEFA a do męskiej drużyny światowej IFFHS , drużyny roku UEFA i drużyny roku ESM po trzy razy .
Oprócz tego został wybrany do `` France Football '' World XI i do drużyny roku Bundesligi .
