Key West
Key West – miasto w Stanach Zjednoczonych , w archipelagu Florida Keys , port turystyczny nad Cieśniną Florydzką o największej głębokości pomiędzy Nowym Orleanem i Norfolkiem .
Znajduje się na wyspie Key West , północnej części Stock Island , Dredgers Key ( znanej jako `` Sigsbee Park '' ) , Fleming Key i Tank Island ( znanej jako `` Sunset Key '' ) .
Na terenie wysp Sigsbee Park i Fleming Key znajduje się baza wojskowa Naval Air Station Key West – z tego powodu wyspy te są niedostępne dla turystów .
