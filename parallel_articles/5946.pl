Uznawane za najbardziej wysunięty na południe punkt kontynentalnych Stanów Zjednoczonych , 171 km na północ od Hawany i 208 km ( 260 km samochodem ) na południowy zachód od Miami .
Najmniejsza odległość od lądu Kuby wynosi 151 km .
