Khwaja Ahmad Abbas
Khwaja Ahmad Abbas ( ur .
7 czerwca 1914 w Panipat , zm .
1 czerwca 1987 w Bombaju ) – indyjski pisarz , dziennikarz , reżyser , scenarzysta i producent filmowy .
Studiował na Uniwersytecie Alighar .
Tworzył w językach urdu i angielskim .
Zasłynął utworami poruszającymi tematy społeczne i religijne .
Jego film `` Wyprawa za trzy morza '' ( 1957 ) startował w konkursie głównym na 11 .
MFF w Cannes .
Abbas zasiadał w jury konkursu głównego na 16 .
MFF w Berlinie ( 1966 ) .
