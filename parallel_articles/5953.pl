Kieren Keke
Kieren Aedogan Keke ( ur .
27 czerwca 1971 w Yaren ) , naurański polityk i lekarz .
Deputowany do parlamentu od 2003 , minister zdrowia w latach 2004-2007 , minister spraw zagranicznych od 2007 , minister finansów od 2008 .
Życiorys .
Kieren Keke , z wykształcenia lekarz , jest jednym z nielicznych profesjonalnych medyków na wyspie .
Jego ojciec , Ludwig Keke , również był lekarzem .
Posiada podwójne - australijskie i nauruańskie - obywatelstwo .
Był jednym z założycieli partii Naoero Amo ( `` Najpierw Nauru '' ) .
Deputowany do parlamentu .
W wyborach z 3 maja 2003 z ramienia Naoero Amo dostał się do parlamentu w okręgu Yaren .
Mandat deputowanego odnawiał w każdych kolejnych wyborach ; w 2004 , 2007 , 2008 i dwukrotnie w 2010 .
