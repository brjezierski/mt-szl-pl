Kilimandżaro ( region )
Kilimandżaro – region ( `` mkoa '' ) w Tanzanii .
W 2002 roku region zamieszkiwało 1 376 702 osób .
W 2012 ludność wynosiła 1 640 087 osób , w tym 793 140 mężczyzn i 846 947 kobiet , zamieszkałych w 384 867 gospodarstwach domowych .
