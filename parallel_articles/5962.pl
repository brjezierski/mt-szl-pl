Kilometr
Kilometr ( symbol : km ) – powszechnie stosowana wielokrotność metra , podstawowej jednostki długości w układzie SI .
Dokładniej , kilometr to 1000 metrów .
Stowarzyszona i dość często używana jednostka powierzchni to kilometr kwadratowy ( symbol : km² ) , a objętości – kilometr sześcienny ( symbol : km³ ) .
