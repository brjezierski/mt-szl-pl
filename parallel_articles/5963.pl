Kim Dzong Un
Kim Dzong Un ( IPA : , kor .
김정은 ; ur .
8 stycznia 1984 w Pjongjangu ) – północnokoreański polityk , marszałek , trzeci syn Kim Dzong Ila – przywódcy KRLD w latach 1994–2011 i wnuk Kim Ir Sena – uznawanego dalej za prezydenta KRLD .
Od 17 grudnia 2011 faktyczny przywódca Korei Północnej , piastujący stanowiska : Naczelnego Dowódcy Koreańskiej Armii Ludowej ( od 2011 ) , Pierwszego Przewodniczącego Komisji Obrony Narodowej ( 2012–2016 ) , Przewodniczącego Komisji Spraw Państwowych Koreańskiej Republiki Ludowo-Demokratycznej ( od 2016 ) , Pierwszego Sekretarza Partii Pracy Korei ( 2012–2016 ) , Przewodniczącego Partii Pracy Korei ( 2016–2021 ) , a następnie Sekretarza Generalnego Partii Pracy Korei ( od 2021 ) .
Życiorys .
Kim Dzong Un urodził się w Pjongjangu .
Jego matką była Ko Yŏng Hŭi , zmarła w 2004 partnerka Kim Dzong Ila .
Matka nazywała go „ Królem Porannej Gwiazdy ” .
