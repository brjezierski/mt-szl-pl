Kim Ir Sen
Kim Ir Sen , właściwie Kim Il Sung ( ; ur .
15 kwietnia 1912 w Man'gyŏngdae w Pjongjangu , zm .
8 lipca 1994 w Pjongjangu ) – koreański polityk , przywódca Korei Północnej w latach 1946–1994 , marszałek i generalissimus Koreańskiej Armii Ludowej , wieloletni przywódca Partii Pracy Korei , od 5 września 1998 „ Wieczny Prezydent ” .
Jest założycielem tzw .
„ dynastii Kimów ” .
Życiorys .
Wczesna działalność .
Kim Hyong Jik , ojciec przywódcy , był działaczem chrześcijańsko-narodowej opozycji wobec japońskiej okupacji Korei .
Mimo starań , jak zapisanie do szkoły misyjnej , nie udało mu się zaszczepić w synu wiary chrześcijańskiej .
Od 1924 Kim Ir Sen przebywał w Chinach , gdzie wstąpił do młodzieżowego ruchu marksistowskiego .
W latach 30 .
XX wieku należał do partyzantki koreańskiej walczącej z okupacją japońską .
Od 1931 należał do Komunistycznej Partii Korei , na której czele stanął w 1945 ( od 1948 jest to Partia Pracy Korei ) .
Zdobył wykształcenie wojskowe i polityczne w ZSRR .
