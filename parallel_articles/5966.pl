Kindu
Kindu ( dawniej `` Kindu Port-Empain '' ) – miasto w Demokratycznej Republice Konga , stolica prowincji Maniema .
Miasto usytuowane jest nad rzeką Kongo , 500 m n.p.m .
Liczba mieszkańców Kindu wynosi około 135,5 tys .
Kindu posiada połączenie kolejowe z Kalemie , Kamina oraz Kananga oraz małe lotnisko z pasem startowym o długości 2200 m .
Miasto niegdyś było ważnym źródłem złota , kości słoniowej oraz w XIX wieku niewolników .
W mieście rozwinął się przemysł drzewny .
