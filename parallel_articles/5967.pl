King Von
King Von , właśc .
Dayvon Daquan Bennett ( ur .
9 sierpnia 1994 w Chicago , zm .
6 listopada 2020 w Atlancie ) – amerykański raper i autor tekstów .
Podpisał kontrakt z wytwórnią rapera Lil Durka , Only the Family i EMPIRE .
Wczesne życie .
King Von urodził się w Chicago .
Większość swojego życia spędził w Parkway Gardens , znanym również jako „ O ’ Block ” , znajdującym się na rogu 64 ulicy i Martin Luther King Drive w południowej części Chicago .
Dorastał ze swoim przyjacielem raperem Lil Durkiem , a także z Chief Keef ’ em .
Był znany z pseudonimu „ Grandson ” , który nawiązuje do Davida Barksdale ’ a , założyciela Black Disciples , którego był członkiem .
Dostał ten pseudonim w więzieniu , kiedy wielu ludzi , którzy znali Davida , powiedziało , że przypominał im przywódcę gangu w tym , jak radzi sobie na ulicach , a także w jego ogólnym zachowaniu .
W wieku 16 lat Von po raz pierwszy trafił do więzienia .
Był to dopiero początek długiego maratonu problemów prawnych rapera .
W 2014 roku został oskarżony o strzelaninę , w której zginęła jedna osoba , a druga została poważnie ranna .
