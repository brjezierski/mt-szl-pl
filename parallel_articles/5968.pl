Kingman ( Arizona )
Kingman – miasto w hrabstwie Mohave , w północno-zachodniej Arizonie , w Stanach Zjednoczonych .
Jest stolicą hrabstwa Mohave .
Miasto posiada powierzchnię 77,6 km² i jest zamieszkiwane przez 20 069 mieszkańców ( stan na 2000 ) .
Znajduje się tam 7854 gospodarstw domowych , zamieszkiwanych przez 5427 rodzin .
Kingman jest położony wzdłuż legendarnej trasy nr 66 .
