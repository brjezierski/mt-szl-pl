Kinszasa
Kinszasa ( dawniej `` Léopoldville '' ) – stolica i największe miasto Demokratycznej Republiki Konga , dawniej stolica Zairu .
Jest to drugie co do wielkości miasto Afryki , po Lagosie w Nigerii , położone na południe od równika .
W 2020 roku miasto zamieszkane było przez 17,1 mln osób .
Kinszasa leży w zachodniej części państwa nad rzeką Kongo , w niewielkiej odległości od stolicy Republiki Konga – Brazzaville .
Podział administracyjny .
Kinszasa jest jednocześnie miastem i jedną z 26 prowincji Demokratycznej Republiki Konga .
Kinszasa jest podzielona na 4 dystrykty , z których z kolei wyodrębniono 24 gminy ( `` communes '' ) .
Gmina Kinszasa dała nazwę całemu miastu , jednak handlowym i administracyjnym sercem miasta jest gmina Gombe .
