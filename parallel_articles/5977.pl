Kioto
– miasto w zachodniej części japońskiej wyspy Honsiu , stolica prefektury Kioto , dawna stolica Japonii i siedziba cesarza .
Kioto jest częścią obszaru metropolitalnego zamieszkiwanego przez ok. 18 mln osób .
Historia .
Pomimo dowodów archeologicznych na istnienie pierwszych osad na wyspach japońskich około 10 tys .
