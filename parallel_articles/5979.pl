Język kipczacki
Język kipczacki ( kumański , połowiecki ) – język z tureckiej rodziny językowej , którego nazwa pochodzi od Kipczaków ( których zachodni odłam był zwany Kumanami lub Połowcami ) , zamieszkujących w XI wieku stepy Azji Środkowej i Europy Wschodniej .
Dawniej język urzędowy Chanatu Kipczackiego , dał początek kipczackiej grupie języków tureckich .
Najstarsze zachowane zabytki spisane zostały w księdze Codex Cumanicus .
Do XVII wieku używany był przez Ormian polskich .
Zbliżonego języka używają do dziś nieliczni Karaimi ( patrz – język karaimski ) .
Utrzymuje się , że ostatnim użytkownikiem języka był István Varró , mieszkaniec Karcagu na Węgrzech , zmarły w 1770 roku .
