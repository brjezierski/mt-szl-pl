Historia .
Kirgistan został podbity przez carską Rosję w 1864 roku .
Pod panowaniem radzieckim od 1919 , początkowo jako Kara-Kirgiski Obwód Autonomiczny , następnie Kirgiska Autonomiczna Socjalistyczna Republika Radziecka , a od 5 grudnia 1936 jako Kirgiska Socjalistyczna Republika Radziecka z Pierwszym Sekretarzem KC Komunistycznej Partii Kirgiskiej SRR jako głową państwa .
Kirgistan uzyskał niepodległość wraz z rozpadem Związku Radzieckiego 31 sierpnia 1991 roku .
Pierwszym prezydentem został bezpartyjny działacz , prezes Kirgiskiej Akademii Nauk , profesor fizyki Askar Akajew .
Był on inicjatorem powołania Wspólnoty Niepodległych Państw i prowadził prokremlowską politykę .
Akajew wprowadził autorytarne rządy prezydenckie .
Zmarginalizowanie roli opozycji pozwoliło mu utrzymać się na stanowisku w wyborach w 1995 i 2000 roku .
W roku 2002 w Kirgistanie miały miejsce pierwsze antyprezydenckie manifestacje .
Na podstawie Rezolucji Rady Bezpieczeństwa ONZ nr 736 z 29 stycznia 1992 Kirgistan został członkiem ONZ .
Domniemane sfałszowanie wyborów parlamentarnych w 2005 oraz narastające tendencje dyktatorskie prezydenta doprowadziły do wybuchu tulipanowej rewolucji , która obaliła Askara Akajewa oraz rząd premiera Nikołaja Tanajewa .
W wyniku rewolucji sformowano Koordynacyjną Radę Jedności Narodowej , która przejęła władzę w kraju , a pełniącym obowiązki prezydenta został były premier Kurmanbek Bakijew z partii Ludowy Ruch Kirgistanu .
