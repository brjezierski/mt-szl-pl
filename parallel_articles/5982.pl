Kiribati
Republika Kiribati ( ang .
`` Republic of Kiribati '' ; kiribati `` Ribaberikin Kiribati '' ) – państwo wyspiarskie na Oceanie Spokojnym .
W skład tego państwa o powierzchni 726 km² wchodzą 32 atole i 1 wyspa koralowa Banaba , rozsiane na powierzchni przeszło 3,5 mln km² wokół równika .
Przez obszar ten przechodzi 180 południk , co powoduje że Kiribati jest jedynym krajem na świecie położonym na wszystkich 4 półkulach Ziemi .
Są to wyspy trzech archipelagów Mikronezji i Polinezji : Wyspy Gilberta ( 16 wysp ) , Feniks ( 8 ) i Line Islands ( 8 ) oraz wyspa Banaba ( zw .
także Ocean ) .
21 z 33 wysp jest zamieszkanych .
