Kirjat Atta
Kirjat Atta ( hebr .
קריית אתא ; arab .
كريات آتا ) – miasto położone w dystrykcie Hajfa w Izraelu .
Jest częścią aglomeracji miejskiej Ha-Kerajot .
Położenie .
Leży w Dolinie Zevulon w Zachodniej Galilei , w otoczeniu miast Hajfa , Kirjat Bialik , Szefaram i Neszer , miasteczka Rechasim , moszawu Kefar Bialik , kibuców Afek , Ramat Jochanan , Kefar ha-Makkabbi i Usza , oraz wiosek Adi , Chawalid , Ibtin i Kefar Chasidim Bet .
Demografia .
Zgodnie z danymi Izraelskiego Centrum Danych Statystycznych w 2008 roku w mieście żyło 49,9 tys .
mieszkańców , z czego 99,8 % Żydzi .
Zgodnie z danymi `` Izraelskiego Centrum Danych Statystycznych '' w Kirjat Atcie w 2000 roku było 17 236 zatrudnionych pracowników i 1226 pracujących na własny rachunek .
Pracownicy otrzymujący stałe pensje zarabiali w 2000 średnio 5157 NIS , i otrzymali w ciągu roku podwyżki średnio o 4,8 % .
