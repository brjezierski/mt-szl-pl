Kirjat Szemona
Kirjat Szemona ( hebr .
קריית שמונה ; arab .
كريات شمونة ; ang .
Qiryat Shemona lub Kiryat Shmona ; pol .
`` Miasto Ośmiu '' ) – miasto w Dystrykcie Północnym , w Izraelu .
Położenie .
Miasto Kirjat Szemona jest położone w północno-zachodnim skraju Palca Galilei – w dolinie Hula w Górnej Galilei , na północy Izraela .
W odległości 1,5 km na zachód i 2,5 km na północny wschód od miasta przebiega granica izraelsko-libańska , znana jako `` Niebieska Linia '' .
W otoczeniu Kirjat Szemona znajdują się kibuce Menara , Misgaw Am , Kefar Giladi , Majan Baruch , Dafna , Ha-Goszerim , Sede Nechemja , Amir , Kefar Blum , Ne ’ ot Mordechaj i Jiftach , moszawy Margalijjot , Juwal i Bet Hillel .
Kirjat Szemona jest położone w Poddystrykcie Safed , w Dystrykcie Północnym Izraela .
