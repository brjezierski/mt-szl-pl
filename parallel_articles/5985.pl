Kirk Douglas
Kirk Douglas , właśc .
Issur Danielovitch ( ur .
9 grudnia 1916 w Amsterdamie w stanie Nowy Jork , zm .
5 lutego 2020 w Beverly Hills ) – amerykański aktor filmowy i telewizyjny .
Laureat Oscara za całokształt twórczości ( w 1996 ) .
Trzykrotnie nominowany do tej nagrody za role pierwszoplanowe w filmach : `` Champion '' ( 1949 ) , `` Piękny i zły '' ( 1952 ) oraz `` Pasja życia '' ( 1956 ) .
Jeden z największych gwiazdorów „ Złotej Ery Hollywood ” .
American Film Institute umieścił go na 17. miejscu na liście największych amerykańskich aktorów wszech czasów ( `` The 50 Greatest American Screen Legends '' ) .
Życiorys .
Wczesne lata .
Urodził się w Amsterdamie w stanie Nowy Jork jako Issur Danielovitch .
Jego rodzice , Bryna „ Bertha ” ( z domu Sanglel ; 1884–1958 ) i Herschel „ Harry ” Danielovitch ( 1884–1950 ) , byli żydowskimi imigrantami z Czausów na terenie dzisiejszej Białorusi .
W 1908 osiedlili się w Amsterdamie , w stanie Nowy Jork .
Obydwoje rodzice byli analfabetami , posługiwali się łamaną angielszczyzną i żyli w ubóstwie .
W wieku 14 lat postanowił zostać aktorem .
W 1934 ukończył Wilbur H. Lynch High School w Amsterdamie .
W latach 1935–1939 studiował język angielski na St. Lawrence University w Canton , w stanie Nowy Jork .
