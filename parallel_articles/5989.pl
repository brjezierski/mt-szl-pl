Kirkland Lake
Kirkland Lake – miasto w Kanadzie , w prowincji Ontario , w dystrykcie Timiskaming .
Nazwa miasta pochodzi o nieistniejącego dziś jeziora Kirkland , które zniknęło w wyniku rozwoju przemysłu górniczego .
Kirkland Lake należy do AFMO , związku gmin ontaryjskich ze znaczącym odsetkiem mieszkańców francuskojęzycznych .
Liczba mieszkańców Kirkland Lake wynosi 8 248 .
Język angielski jest językiem ojczystym dla 77,4 % , francuski dla 14,9 % mieszkańców ( 2006 ) .
