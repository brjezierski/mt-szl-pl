Kiss ( album Kiss )
Kiss – debiutancki album amerykańskiej grupy rockowej Kiss wydany w lutym 1974 roku .
W chwili wydania albumu zespół istniał dopiero rok , więc większość utworów na albumie jest autorstwa Gene ’ a Simmonsa i Paul Stanleya lub są to piosenki z repertuaru zespołu poprzedzającego Kiss .
Simmons szacuje , iż cały proces nagrywania i miksowania albumu trwał trzy tygodnie , zaś koproducent Richie Wise uważa , iż grupa miała tylko trzynaście dni .
Płyta została nagrana w Bell Sound Studios w Nowym Jorku .
