Kisumu ( hrabstwo )
Kisumu – jedno z 47 hrabstw Kenii .
Jego siedzibą jest miasto Kisumu .
Liczy 1 155 574 mieszkańców ( 2019 ) , z czego większość to grupa etniczna Luo .
Hrabstwo Kisumu graniczy z Jeziorem Wiktorii na zachodzie , oraz z hrabstwami : Siaya , Vihiga i Nandi na północy , z Kericho na wschodzie i z Homa Bay na południu .
Rolnictwo .
Główną działalnością hrabstwa jest rybołówstwo .
Główne uprawy w regionie to : kukurydza , fasola , słodkie ziemniaki , sorgo i
maniok , a także nawadnia się pomidory i kapustę .
Produkcja zwierzęca ma zazwyczaj charakter tradycyjny .
Główne gatunki hodowlane w hrabstwie obejmują bydło zebu , lokalny drób i kozy .
