Kitakiusiu
– miasto przemysłowe w Japonii , w prefekturze Fukuoka , na wyspie Kiusiu ( `` Kyūshū '' ) .
Opis .
Miasto jest ważnym węzłem komunikacyjnym ruchu lądowego i morskiego .
Jest łącznikiem pomiędzy wyspami Honsiu ( `` Honshū '' ) a Kiusiu i ważnym portem handlu międzynarodowego .
Kitakiusiu zostało utworzone w 1963 roku poprzez połączenie pięciu miast : Kokury , Wakamatsu , Yahaty , Tobaty , Moji .
W latach 60. i 70 .
XX wieku w Kitakiusiu w wyniku dynamicznego rozwoju przemysłu ciężkiego i chemicznego doprowadzono do znacznego skażenia rzek , wód przybrzeżnych , gleby i powietrza .
Miasto zasłynęło ze zjawisk wielobarwnego nieba nocą , co było skutkiem oświetlania gęstego smogu miejskimi światłami .
