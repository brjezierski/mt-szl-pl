Język kitański
Język kitański – wymarły język wywodzący się grupy języków ałtajskich używany w Mongolii , Mandżurii i północnych Chinach .
Ostatni użytkownicy wymarli zapewne ok. XIII wieku , zachowały się jednak inskrypcje w piśmie Kitanów , które pozostają w większości nieodczytane .
Uważa się , że kitański był blisko spokrewniony ze staromongolskim .
