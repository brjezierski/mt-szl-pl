Kitee
Kitee ( szw .
`` Kides '' ) – miasto i gmina w Finlandii .
Jako gmina istnieje od 1631 roku , natomiast jako miasto od 1992 .
Miasto znajduje się w dawnej prowincji Finlandia Wschodnia i jest częścią regionu Karelia Północna .
Populacja gminy wynosi 10 024 ( 2003 ) , zajmuje powierzchnię 1141,75 km² , z czego 276,87 km² ( prawie 25 % ) to woda .
Gęstość zaludnienia wynosi 8,8 mieszkańca na km² .
W Kitee urodziła się Tarja Turunen i powstał metalowy zespół Nightwish .
