Kiwerce
Kiwerce ( ) – miasto na Ukrainie i stolica rejonu w obwodzie wołyńskim .
Leży na Wołyniu .
Węzeł kolejowy .
Historia .
Kiwerce założono w 1870 w związku z budową linii kolejowej Żytomierz-Kowel i do dziś to miasto jest ważnym węzłem kolejowym .
W Kiwercach od głównej trasy kolejowej odchodzi także odnoga prowadząca do pobliskiego Łucka .
W okresie II Rzeczypospolitej Kiwerce były siedzibą gminy wchodzącej w skład powiatu łuckiego .
W 1923 r. erygowana została Parafia Najświętszego Serca Jezusowego w Kiwercach .
