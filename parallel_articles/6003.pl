Kisielany-Kuce
Kisielany-Kuce – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Leży 11 km na północny zachód od Siedlec .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Marii Magdaleny w Suchożebrach .
Mieszkańcy zajmują się głównie rolnictwem ( uprawa zbóż i ziemniaków , sadownictwo , hodowla bydła i trzody chlewnej ) lub pracą w pobliskich Siedlcach .
Na terenie wsi działa także zakład wylęgu drobiu należący do ogólnopolskiej firmy Drosed .
Do 1999 we wsi działała także szkoła podstawowa , w której prowadzono nauczanie początkowe dla dzieci z Kisielan-Kuców i Kisielan-Żmichów .
Obecnie dzieci pochodzące z obydwu tych miejscowości uczęszczają do SP w Mokobodach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
