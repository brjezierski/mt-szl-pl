Kisielany-Żmichy
Kisielany-Żmichy – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Wieś położona na bardzo zróżnicowanym terenie .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Marii Magdaleny w Suchożebrach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Mieszkańcy zajmują się przede wszystkim sadownictwem lub pracują w pobliskich Siedlcach .
W Kisielanach znajduje się placówka opiekuńczo-wychowawcza `` Dom Dziecka Na Zielonym Wzgórzu '' .
We wsi działa założona w 1920 roku jednostka ochotniczej straży pożarnej .
