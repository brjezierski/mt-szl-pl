Kiustendił ( stacja kolejowa )
Kiustendił ( ) – stacja kolejowa w miejscowości Kiustendił , w obwodzie Wraca , w Bułgarii .
Znajduje się na linii Sofia – Giuszewo .
Historia .
Została zbudowana w 1897–1906 i została otwarta wraz z liną Radomir–Kiustendił w 1909 .
Po 1944 roku stacja została przebudowana i zmodernizowana , aby zwiększyć swoją przepustowość .
