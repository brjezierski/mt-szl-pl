Klagenfurt am Wörthersee
Klagenfurt am Wörthersee ( do 31 stycznia 2008 `` Klagenfurt '' ; słoweń .
`` Celovec ob Vrbskem Jezeru '' ; pol .
hist .
`` Celowiec '' ) – miasto statutarne w południowej Austrii , stolica kraju związkowego Karyntia oraz powiatu Klagenfurt-Land do którego jednak miasto nie należy .
Liczy 97827 mieszkańców ( 1 stycznia 2015 ) .
Jest to największe miasto Karyntii i szóste pod względem wielkości miasto Austrii .
Malownicze położenie Klagenfurtu am Wörthersee w terenie górzystym zainspirowało mieszkańców do zbudowania tam kompleksu dla sportowców i kadry narodowej Austrii .
Ośrodek posiada starannie zaprojektowaną i wykonaną infrastrukturę .
