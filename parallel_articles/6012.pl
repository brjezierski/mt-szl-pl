Suknia
Suknia , sukienka – wierzchni jednoczęściowy strój , okrywający tułów i nogi , często także ręce .
Suknia wyewoluowała z wczesnośredniowiecznych płaszczy i peleryn na skutek zmiany ich kroju i udoskonalenia tkanin .
Za moment pojawienia się sukni uważa się upowszechnienie w połowie XV wieku wąskiego , dopasowanego do sylwetki stroju `` cotardie '' ( istniało męskie i kobiece ) .
`` Cotardie '' kobiece miało wysoki stanik , rozszerzany do połowy bioder i przechodzący w spódnicę .
Stanik z przodu miał głębokie wcięcie i u góry był zakończony kołnierzem .
Taki krój sukni optycznie wyszczuplał sylwetkę .
