Dźwięk ( muzyka )
Dźwięk muzyczny – dźwięk wytwarzany przez instrument muzyczny ( albo głos ludzki ) .
Najczęściej jest to dźwięk o określonej wysokości .
Głównymi cechami dźwięku są :
Podstawowym zestawem dźwięków muzycznych jest skala ( gama ) .
W tradycyjnej muzyce europejskiej tworzy ją osiem dźwięków , z których ósmy jest powtórzeniem pierwszego w interwale oktawy , tzn .
częstotliwość ósmego oddalona jest od częstotliwości pierwszego o odległość oktawy .
Ten ósmy dźwięk jest zarazem pierwszym dźwiękiem kolejnej ósemki od którego następny ósmy ( piętnasty ) znów jest oddalony o oktawę itd .
Odległość należy tu rozumieć nie potocznie jako różnicę częstotliwości , lecz jako ich stosunek ( iloraz ) , tzn .
częstotliwość każdego kolejnego dźwięku jest ileś razy większa , konkretnie dla odległości oktawy jest 2 razy większa .
Dlatego prawidłowym określeniem jest interwał , o odległości można mówić potocznie .
Zakres dźwięków tradycyjnie używanych w muzyce podzielony jest na 10 oktaw posiadających swoje nazwy .
Oktawa jest jednym z interwałów , ale słowo to posiada też drugie znaczenie jako zestaw dźwięków danej ósemki , a właściwie siódemki bo ósmy dźwięk jest pierwszym kolejnej oktawy .
Każda oktawa zawiera siedem dźwięków diatonicznych , tworzących szereg określany w systemie dur-moll jako gama C-dur .
Kolejno są to C , D , E , F , G , A , H ( w angielskiej notacji B ) .
Dźwięki posiadają też nazwy solmizacyjne kolejno : do , re , mi , fa , sol , la , si .
Kolejne C jest pierwszym dźwiękiem następnej oktawy .
Osiem dźwięków występujących w gamie C-dur zwane jest naturalnym szeregiem diatonicznym .
Dźwięki te odpowiadają białym klawiszom fortepianu .
Podstawowym dźwiękiem każdej oktawy jest dźwięk C. Każde C jest oddalone od poprzedniego o interwał oktawy , tzn .
częstotliwości kolejnych dźwięków C są 2 razy większe każda od poprzedniej , czyli są elementami ciągu geometrycznego o ilorazie 2 , przy czym najniższemu C odpowiada w przybliżeniu dźwięk o częstotliwości 16 Hz .
To samo dotyczy pozostałych dźwięków , np .
kolejne D jest wyższe ( niższe ) od poprzedniego o oktawę i ma 2 razy większą ( mniejszą ) częstotliwość .
O interwałach pomiędzy dźwiękami o różnych nazwach np .
C i D patrz niżej .
Każdy interwał wyraża się w jednostkach zwanych półtonami , oktawa zawiera dokładnie 12 półtonów .
Definiuje to wartość półtonu jako formula_1 bo złożenie dwunastu półtonów daje oktawę , czyli przemnożenie wartości półtonu 12 razy daje liczbę 2 .
Można powiedzieć , że oktawa jest nazwą jednego z kilku rodzajów interwałów , a półton jest jednostką interwału .
Dźwięk jest wyższy od drugiego o jeden półton gdy jego częstotliwość jest większa od częstotliwości drugiego formula_2 razy , wyższy o 2 półtony gdy częstotliwość jest większa formula_3 razy , o 3 półtony gdy częstotliwość jest większa formula_4 razy itd .
Półton jest najmniejszą odległością między dźwiękami stosowaną w muzyce europejskiej .
