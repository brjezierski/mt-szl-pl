Klatovy ( stacja kolejowa )
Klatovy – stacja kolejowa w Klatovy , w kraju pilzneńskim , w Czechach .
Jest największą i główną stacją w mieście , położoną w północno-zachodniej części miasta .
Znajduje się na wysokości 400 m n.p.m .
Na stacji istnieje możliwość zakupu biletów na wszystkiego rodzaju pociągi oraz rezerwacji miejsc .
