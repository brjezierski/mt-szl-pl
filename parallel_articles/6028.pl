Klimczok
Klimczok , dawniej również `` Klimczak '' ( 1117 m n.p.m . )
– szczyt górski w północno-wschodniej części Pasma Baraniej Góry w Beskidzie Śląskim .
Przebiega przezeń granica administracyjna Bielska-Białej i stąd szczyt Klimczoka jest najwyższym punktem tego miasta .
Przez szczyt biegnie też historyczna granica pomiędzy Śląskiem i Małopolską .
Opis .
Usytuowany w grzbiecie , podchodzącym tu od południowego zachodu , z Trzech Kopców , jest Klimczok zwornikiem dla grzbietu opadającego łagodnie na północ ku Szyndzielni oraz drugiego , odchodzącego na wschód ku Magurze .
Wierzchołek Klimczoka ma kształt kopca o zaokrąglonym szczycie .
Grzbiety odchodzące od niego są dość szerokie , za to stoki opadające ku dolinom Białki ( na północnym wschodzie ) , Biłej ( na południu ) i Barbary ( na zachodzie ) są strome i porośnięte bukowo-świerkowymi lasami .
Na północno-wschodnich stokach Klimczoka znajdują się źródła Białej .
W zboczach Klimczoka znajdują się dwie jaskinie : Jaskinia Ali Baby w Klimczoku i Jaskinia Piętrowa w Klimczoku .
Na szczycie znajduje się stalowy maszt z antenami przekaźników telekomunikacyjnych .
Z istniejącej do końca lat 70 .
XX w. na wierzchołku Klimczoka drewnianej wieży triangulacyjnej widać było znaczną część Karpat Zachodnich od Łysej Góry na zachodzie po Tatry na południowym wschodzie .
Dziś , z uwagi na podrastające wokół wierzchołka drzewa , widok ogranicza się jedynie do sektora południowo-wschodniego , w którym ponad grzbietami Beskidu Żywieckiego królują szczyty Tatr .
Z rzadkich w Polsce gatunków roślin rośnie na Klimczoku tojad morawski .
Na Klimczoku w granicach leśnictwa Biła ( Nadleśnictwo Bielsko ) rośnie grupa drzew przekraczających 45 metrów wysokości , wśród których kilka przekracza 53 m , a jedna daglezja zielona na początku 2018 roku osiągała ok. 57 metrów ( dokładny wynik zależy od sposobu pomiaru ) , a jej wiek był szacowany na 115 lat .
Klimat .
Na Klimczoku średnia roczna temperatura wynosi 5–6 °C .
Pokrywa śnieżna o grubości ok. 220 cm utrzymuje się przez około 125 dni w roku , od początku listopada do początku maja .
Maksymalną pokrywę śnieżną na Klimczoku zanotowano 14 marca 1993 i wyniosła ona 305 cm .
Klimczok cechują duże opady do 1300 mm rocznie .
Przez około 79 dni w roku świeci słońce .
Wybierając się na Klimczok warto zabrać ze sobą coś przeciwdeszczowego .
Nazwa .
Jeszcze na początku XIX w. góra nazywana była `` Goryczną Skałką '' lub po prostu `` Skałką '' .
Na zachodnich zboczach góry , tuż pod szczytem , ciągnie się system rowów grzbietowych i rozpadlin skalnych ( skąd pierwotna nazwa ) , w których znajduje się kilka niewielkich jaskiń .
Największa z nich , długości 26 m , miała być według lokalnych podań kryjówką słynnego zbójnika żywiecko-śląskiego pogranicza – Klimczoka .
Od jego nazwiska w epoce romantyzmu poszła obecna nazwa szczytu .
Turystyka .
W siodle pomiędzy Klimczokiem i Magurą ( Siodło pod Klimczokiem ) znajduje się skrzyżowanie szlaków pieszych do Szczyrku , Bystrej , Brennej na Przełęcz Salmopolską , Przełęcz Karkoszczonkę , Szyndzielnię i Błatnią .
Do nazwy szczytu nawiązuje schronisko PTTK Klimczok , znajdujące się w odległości 700 m na wschód .
Na polanie , pokrywającej wschodnie ramię Klimczoka opadające ku Siodłu pod Klimczokiem , działa narciarski wyciąg talerzykowy długości 430 m .
