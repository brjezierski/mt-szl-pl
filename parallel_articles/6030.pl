Klimontów
Klimontów – miasto w Polsce położone w województwie świętokrzyskim , w powiecie sandomierskim , na Wyżynie Sandomierskiej , około 30 km na zachód od Sandomierza .
Siedziba gminy Klimontów .
Klimontów uzyskał lokację miejską w 1604 roku , zdegradowany ukazem carskim w 1870 roku do rangi osady , od 1 stycznia 2020 r. ponownie miasto .
W latach 1975–1998 miejscowość należała do województwa tarnobrzeskiego .
Przez miasto przebiega droga krajowa nr 9 z Radomia do Rzeszowa , a także droga wojewódzka nr 758 .
Klimontów znajduje się na Małopolskiej Drodze św .
Jakuba .
Historia .
Wieś Klimontów powstała w połowie XIII w. Znajdowała się na terenie współczesnej miejscowości Górki .
Obok niej , na gruntach wsi `` Ramuntowice '' ( lub `` Ramułtowice '' ) powstało miasto .
Założył je na mocy królewskiego przywileju Jan Zbigniew Ossoliński 2 stycznia 1604 roku .
