Trzepaczka do dywanów
Trzepaczka do dywanów – proste ręczne narzędzie służące do czyszczenia dywanów , a dokładniej do usuwania z zalegających w ich włosiu cząstek stałych .
W etnolekcie śląskim trzepaczka do dywanów znana jest powszechnie jako `` kloper '' .
Budowa .
Tradycyjne trzepaczki robione były z wikliny .
Spotyka się także modele zrobione z tworzyw sztucznych lub metalu osłoniętego tworzywem ( najczęściej jest to plecionka odpowiednio grubego drutu w izolacji z tworzywa sztucznego ) .
