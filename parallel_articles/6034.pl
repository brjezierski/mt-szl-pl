Klosterneuburg
Klosterneuburg – miasto powiatowe w północno-wschodniej Austrii , w kraju związkowym Dolna Austria , siedziba powiatu Wien-Umgebung , leży nad Dunajem .
Według danych z 1 stycznia 2014 liczyło 26 174 mieszkańców .
Znajduje się tutaj średniowieczne opactwo .
Geografia .
Granice administracyjne Klosterneuburga bezpośrednio sąsiadują z granicami administracyjnymi Wiednia , od którego Klosterneuburg oddzielony jest dwoma wzgórzami : Kahlenberg i Leopoldsberg , a granica między tymi miastami jest jednocześnie granicą dwóch krajów związkowych : kraju związkowego Wiedeń i Dolnej Austrii .
