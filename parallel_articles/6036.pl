Kluczbork
Kluczbork ( niem .
`` Kreuzburg '' ) – miasto w województwie opolskim , w powiecie kluczborskim , siedziba gminy miejsko-wiejskiej Kluczbork .
Leży na pograniczu Niziny Śląskiej i Wyżyny Śląskiej .
Przepływa przez nie rzeka Stobrawa .
W latach 1975–1998 miasto administracyjnie należało do województwa opolskiego .
Według danych na 30 czerwca 2019 miasto było zamieszkane przez 23 554 osób .
Geografia .
Położenie .
Miasto jest położone w południowo-zachodniej Polsce , w województwie opolskim , na północno-wschodnim krańcu Niziny Śląskiej , na pograniczu z Wyżyną Śląską .
Kluczbork leży na historycznym Dolnym Śląsku , od XIV wieku do 1675 roku położony był w dolnośląskim księstwie brzeskim .
Na początku XIX wieku został wraz z jednoimiennym powiatem włączony do rejencji opolskiej ze stolicą w Opolu , w związku z czym bywa również zaliczany do Górnego Śląska ( w XIX wieku do nazwy `` Kreuzburg '' dodawano często `` O.S . ''
– `` Oberschlesien '' ) .
Stosunki wodne .
Przez granice administracyjne miasta przepływa rzeka Stobrawa ( prawy dopływ Odry w jej górnym biegu ) , a także potok Baryczka Mała .
Stobrawa jest niewielkim ciekiem wodnym , a jej dolina jest tu jeszcze wąska i niezbyt głęboka .
Miasto posiada kilka zbiorników wodnych – obecnie można wyróżnić Staw Kościuszki ( dawniej Millera ) oraz staw w Parku miejskim .
Niedawno został oddany zbiornik retencyjny , który swoją powierzchnią pokrył żwirownię .
Jego powierzchnia to ok. 60 hektarów .
Zbiornik retencyjny w dolinie rzeki Stobrawy jest położony na wysokości wsi Ligoty Górnej , na terenach leżących w gminach Kluczbork i Lasowice Wielkie .
Zbiornik wodny powstał na skutek zalania ok. 60 ha gruntów rolnych .
Zalaniu uległo około 1,5-kilometrowy odcinek doliny rzeki Stobrawy i przyległych jej gruntów .
Środowisko naturalne .
Średnia temperatura roczna wynosi +8,4 °C .
Pokrywa śnieżna występuje od grudnia do kwietnia .
Duże zróżnicowanie dotyczy termicznych pór roku .
Średnie roczne opady atmosferyczne w rejonie Kluczborka wynoszą 586 mm , dominują wiatry zachodnie .
Najcieplejszym miesiącem jest lipiec ze średnią temperaturą 17,6 – 17,9 °C , a najzimniejszym styczeń z temperaturą od -1,5 do 2,2 °C .
Okres wegetacyjny trwa ok. 220 dni .
