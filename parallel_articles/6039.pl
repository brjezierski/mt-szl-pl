Klusy ( powiat piski )
Klusy ( niem .
`` Klaussen '' albo `` Claussen '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie piskim , w gminie Orzysz .
Do 1954 roku miejscowość była siedzibą gminy Klusy .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
Wieś kościelna na przesmyku pomiędzy jeziorem Kraksztyn i Lipińskim .
