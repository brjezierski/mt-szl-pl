Osówiec Kmiecy
Osówiec Kmiecy – wieś w Polsce położona w województwie mazowieckim , w powiecie przasnyskim , w gminie Przasnysz .
Sołectwo Osówiec Kmiecy obejmuje wsie Osówiec Kmiecy oraz Patołekę .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Stanisława Biskupa w Świętym Miejscu .
Pierwsze wzmianki o wsi pochodzą z 1438 roku , kiedy to braciom Piotrowi , Mikołajowi , Janowi i Bieńkowi z Niestumia nadane zostało przez księcia 30 włók o nazwie Ossowyecz .
W 1567 wieś nosiła nazwę Osówiec Wielki , a w jej skład wchodziły 4 działy liczące 5 włók , 3 ogrodników i 2 rzeźników .
Na początku XIX wieku , licząc 10 domów , wraz z folwarkiem Łyszkowo znajdowała się w gminie Bartniki .
W 1886 roku należała do gminy Karwacz .
