Knittelfeld
Knittelfeld – miasto w Austrii , w kraju związkowym Styria , w powiecie Murtal .
Do 31 grudnia 2011 siedziba powiatu Knittelfeld .
Leży nad rzeką Murą .
Liczy 12 541 mieszkańców ( 1 stycznia 2015 ) .
Gospodarka .
W mieście rozwinął się przemysł metalowy oraz drzewny , funkcjonują też zakłady naprawcze taboru kolejowego .
