Kniżownik
Kniżownik ( bułg .
`` Книжовник '' ) – wieś w południowej Bułgarii , w obwodzie Chaskowo , w gminie Chaskowo .
Według danych Narodowego Instytutu Statystycznego w Bułgarii , 31 grudnia 2011 roku wieś liczyła 576 mieszkańców .
Wieś położona 15 km na południowy wschód od Chaskowa .
Przez wieś przebiega linia kolejowa do Ruse .
Dawna , pochodząca z języka tureckiego nazwa miejscowości to Idebik .
We wsi znajduje się stadion imienia piłkarza bułgarskiego Petyra Żekowa .
