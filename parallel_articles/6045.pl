Knorydy
Knorydy – wieś w Polsce położona w województwie podlaskim , w powiecie bielskim , w gminie Bielsk Podlaski .
Prawosławni mieszkańcy wsi należą do parafii Zaśnięcia Matki Bożej w Boćkach , a wierni kościoła rzymskokatolickiego do parafii św .
Józefa Oblubieńca w Boćkach .
Historia .
W 1673 roku właścicielem wsi był koniuszy litewski Franciszek Stefan Sapieha .
