Knutsford
Knutsford – miasto i civil parish w Wielkiej Brytanii hrabstwie Cheshire , w dystrykcie ( unitary authority ) Cheshire East .
Położone na południowy zachód od Manchesteru i północny zachód Macclesfield .
W 2011 roku civil parish liczyła 13 191 mieszkańców .
Miasto położone jest przy linii kolejowej z Chester do Manchesteru – stacja Knutsford powstała w roku 1862 .
W pobliżu miasta przebiegają autostrady M6 oraz M56 , a przez samą miejscowość także mocno obciążona tranzytem droga A50 .
Knutsford jest wspomniana w Domesday Book ( 1086 ) jako `` Cunetesford '' .
