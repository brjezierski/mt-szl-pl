Kobe
– miasto portowe w Japonii , położone na wschodnim wybrzeżu wyspy Honsiu ( `` Honshū '' ) pomiędzy zatoką Osaka ( `` Ōsaka-wan '' ) a łańcuchem niskich gór Rokkō ( `` Rokkō-sankei '' ) o długości 56 km .
Ich częścią jest pasmo również niewysokich gór o nazwie Tanjō-sankei lub Tanjō-Taishaku .
Najwyższym szczytem jest Rokkō ( 931 m ) .
Kobe jest stolicą prefektury Hyōgo i jednym z dziesięciu największych miast Japonii .
Leży w regionie Kinki ( Kansai ) , 30 km na zachód od Osaki .
Należy do obszaru metropolitalnego Keihanshin liczącego ok. 18 mln mieszkańców .
Miasto dzieli się na dziewięć dzielnic : Nishi , Kita , Tarumi , Suma , Nagata , Hyōgo , Chūō , Nada , Higashi-Nada .
Historia .
Znaleziska archeologiczne wskazują na istnienie osad ludzkich na terenie zachodniej części dzisiejszego Kobe już w okresie Jōmon ( lata 12 000 p.n.e .
do 300 p.n.e ) .
Region Kobe jest wzmiankowany w starej kronice Nihon-shoki , opisującej założenie chramu Ikuta przez cesarzową Jingū w roku 201 n.e .
