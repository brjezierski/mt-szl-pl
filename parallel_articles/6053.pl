Kobelaky
Kobelaky ( ) – miasto na Ukrainie , w obwodzie połtawskim , siedziba władz rejonu kobelackiego .
Ośrodek przemysłu spożywczego .
Historia .
Od 1803 do 1923 roku w guberni połtawskiej .
W 1919 roku zaczęto wydawać gazetę .
Od 15 września 1941 roku do 25 września 1943 roku miasta było pod okupacją niemiecką .
Podczas okupacji w mieście powstał oboz koncentracyjny
W 1989 liczyły 12 975 mieszkańców .
W 2013 liczyły 10 181 mieszkańców .
