Kobiela ( szczyt )
Kobiela ( niem .
`` Wilhelmshöhe '' ) – szczyt w południowej części Gór Czarnych w Sudetach Środkowych o wysokości 587 ( 8 ) m n.p.m .
Szczyt zasłania od południowego zachodu część uzdrowiskową miasta Jedlina-Zdrój .
W latach 40 .
XIX w. na szczycie utworzono park według założenia C. C. Beinerta z altanami , punktami widokowymi , pomnikami .
