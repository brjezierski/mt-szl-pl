Koblencja
Koblencja ( niem .
`` Koblenz '' , fr .
`` Coblence '' ; do 1926 roku `` Coblenz '' ) – miasto na prawach powiatu w zachodniej części Niemiec , w kraju związkowym Nadrenia-Palatynat , siedziba powiatu Mayen-Koblenz , port rzeczny u ujścia Mozeli do Renu .
Historia .
Miasto zostało założone około 9 roku p.n.e .
jako rzymskie `` castellum '' ( reduta ) .
W starożytności leżało w granicach Cesarstwa Rzymskiego , z kolei we wczesnym średniowieczu znajdowało się we władaniu frankijskim i lotaryńskim , a od 1018 roku było w posiadaniu arcybiskupów Trewiru ( od XIV wieku Elektoratu Trewiru ) .
Od XI do XII wieku było ośrodkiem handlu winem .
