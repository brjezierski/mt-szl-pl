Koblów
Koblów ( `` czes . ''
Koblov , `` niem . ''
Koblau ) jest byłą wsią w Czechach znajdującą się na lewym brzegu Odry , pod wzgórzem `` Landek '' .
Obecnie jest częścią składającą się na ostrawską dzielnicę Śląska Ostrawa .
Nazwa wywodzi się od miejsca wypasania się klaczy , które znajdowało się na terenie wsi .
Obszar Koblowa wynosi 568,54 ha , a w 2001 mieszkało tu 1156 osób , co dawało gęstość zaludnienia równą 203,3 os./km² .
Pierwsze wzmianki pochodzą z 1377 z niemieckojęzycznego listu o podziale księstwa opawskiego przez dziedziców Mikołaja II .
Koblów ( wraz z landeckim grodem ) stał się własnością książąt Wacława i Przemka .
Sama wieś jest jednak dużo starsza , a o długotrwałym zasiedleniu świadczy np .
odnaleziony w osadach Odry miecz z okresu kultury łużyckiej sprzed dwóch tysięcy lat .
W 1742 po przegranej przez Austrię wojen śląskich Koblów znalazł się w granicach Prus , gdzie przejęli go jezuici .
Po wielu zmianach właścicieli ostatecznie zakupił je w 1846 Salomon Mayer Rothschild .
Aż do początku XIX wieku mieszkańcy Koblowa żyli tylko z rolnictwa .
Zmiana nastąpiła , jak w większości gmin w okolicach Ostrawy , po odkryciu złóż węgla .
Wydobycie zaczęło się w 1803 a w efekcie miejscowość nękały szkody górnicze , aż do dziś .
W 1920 Koblów znalazł się w granicach Czechosłowacji na ziemi hulczyńskiej .
Przed II wojną światową , większość ludność zadeklarowało narodowość czeską ( Morawcy ) , ale podczas wyborów w maju 1938 dwie trzecie mieszkańców zagłosowała na Partię Sudeckoniemiecką .
Duży wpływ na tak ogromne poparcie dla Niemców miała germanizacji z czasów przynależności do Prus .
W wyniku układu monachijskiego od 10 października wieś stała się częścią III Rzeszy , co większa część mieszkańców przywitała z zadowoleniem .
Armia Czerwona wydobyła miejscowość spod niemieckiej władzy 30 kwietnia 1945 roku .
Negatywny wpływ na miejscowość miało także wydobywanie od 1957 żwiru .
Koblów znalazł się w granicach administracyjnych Ostrawy 6 kwietnia 1976 .
