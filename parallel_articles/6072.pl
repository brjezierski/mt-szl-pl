Chocim ( województwo opolskie )
Chocim ( , , ) – osada w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik , wchodząca w skład sołectwa Łąka Prudnicka .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Według danych na 2016 osada była zamieszkana przez 33 osoby .
Geografia .
Chocim położony jest na zachodnim stoku wzniesienia Okopowa , na wysokości 300–315 m n.p.m. , około 3,5 km od granicy z Czechami , w okolicy Lasu Prudnickiego .
Nazwa .
Do 1936 używana była niemiecka nazwa `` Kotzem '' .
Ze względu na jej polskie pochodzenie , w 1936 w miejsce nazwy nazistowska administracja III Rzeszy wprowadziła nową , niemiecką – `` Linden '' .
1 października 1948 r. nadano miejscowości , wówczas administracyjnie związanej z Łąką Prudnicką , polską nazwę Chocim .
Nazywana też `` Kocym '' .
Historia .
Wieś Chocim założył w 1255 przebywający wówczas w Opawie najwyższy marszałek Królestwa Czech i namiestnik Styrii – czeski wielmoża Wok z Rożemberka ( Woko de Rosenberch , Vok I. z Rožmberka ) pod nazwą Stary Chocim ( niem .
`` Alt Kotzem '' ) .
1 czerwca 1259 podarował ją klasztorowi cystersów w Vyššim Brodě , a 29 maja 1261 odnowił darowiznę .
