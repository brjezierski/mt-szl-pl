Koczin
Koczin ( malajalam കൊച്ചി , trl .
`` Kocci '' , trb .
`` Koćći '' ; hindi कोच्चि , trl .
`` Koćći '' , trb .
`` Koćći '' ; ang .
Kochi ; dawniej : hindi कोचीन , trl .
`` Koćīn '' , trb .
`` Koćin '' ; ang .
`` Cochin '' ) – miasto w południowej części Indii , w stanie Kerala , na Wybrzeżu Malabarskim nad Morzem Arabskim , będące jednym z najważniejszych portów kraju .
Jest zamieszkane przez około sześćset tysięcy ludzi , a wraz z całą aglomeracją populacja wynosi około półtora miliona .
Począwszy od VIII wieku centralny ośrodek diaspory żydowskiej na subkontynencie indyjskim .
W doskonałym stanie zachowała się synagoga Paradesi i inne ślady obecności Hebrajczyków w mieście .
Od 1102 Koczin był stolicą Królestwa Koczinu .
Od XIV wieku stanowił ważne centrum handlu przyprawami .
W 1503 znalazł się pod okupacją portugalską , stając się pierwszą europejską kolonią w Indiach i do 1530 pozostał stolicą portugalskich posiadłości w Indiach , a do 1663 strategicznym ośrodkiem portugalskiego imperium kolonialnego .
Następnie miasto przechodziło w ręce Holandii , Mysore i Imperium Brytyjskiego .
W 1947 przyłączyło się do Indii .
