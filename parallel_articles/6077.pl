Kofi Annan
Kofi Atta Annan ( ur .
8 kwietnia 1938 w Kumasi , zm .
18 sierpnia 2018 w Bernie ) – ghański dyplomata i mąż stanu , w latach 1997–2006 sekretarz generalny Organizacji Narodów Zjednoczonych , laureat Pokojowej Nagrody Nobla ( 2001 ) .
Życiorys .
Urodził się 8 kwietnia 1938 w Kumasi na terenie brytyjskiego Złotego Wybrzeża .
Ukończył studia na Wydziale Ekonomii Politechniki w rodzinnym Kumasi ( `` University of Science and Technology '' ) oraz w Macalester College w Saint Paul ( tytuł B.A . )
w Minnesocie w USA .
Później studiował na Wydziale Ekonomii w Institut Universitaire des Hautes Etudes Internationales w Genewie i Massachusetts Institute of Technology , gdzie otrzymał tytuł magistra zarządzania .
W różnych agendach ONZ Kofi Annan pracował od 1962 , stopniowo osiągając kolejne pozycje w hierarchii tej organizacji .
W latach 90 .
XX wieku pracował m.in .
jako asystent sekretarza generalnego ds .
operacji pokojowych ( marzec 1993 – luty 1994 ) oraz jako zastępca sekretarza generalnego Butrusa Ghalego ( luty 1994 – październik 1995 ; kwiecień 1996 – grudzień 1996 ) .
Od listopada 1995 do marca 1996 , w okresie po podpisaniu porozumienia pokojowego w Dayton był specjalnym przedstawicielem sekretarza generalnego w byłej Jugosławii , nadzorując wycofywanie Sił Ochronnych ONZ ( UNPROFOR ) z regionu i zastępowanie ich siłami NATO ( IFOR ) .
Pierwszą kadencję na stanowisku sekretarza generalnego rozpoczął 1 stycznia 1997 roku .
Wkrótce po uzyskaniu nominacji Annan wystąpił z inicjatywą reformy ONZ .
1 stycznia 2002 rozpoczął drugą kadencję urzędowania na tym stanowisku , która trwała do 31 grudnia 2006 roku .
W 2012 został wybrany specjalnym wysłannikiem ONZ ds .
Syrii .
Jego żoną była Nane Annan ze Szwecji , z wykształcenia prawnik , obecnie zajmująca się sztuką .
Mieli troje dzieci .
