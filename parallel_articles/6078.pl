Kojšov
Kojšov – wieś ( obec ) na Słowacji położona w kraju koszyckim , w powiecie Gelnica , w historycznym regionie Spisz .
Położenie .
Leży w górach , w Rudawach Spiskich , w północnej części Pasma Kojszowskiej Hali ( Góry Wołowskie ) .
Zabudowania rozłożone są w niewielkiej kotlince , stanowiącej rozszerzenie wąskiej poza tym doliny Kojšovského potoku .
Jedyna droga publiczna , umożliwiająca dojazd do wsi , wiedzie z Jakloviec w dolinie Hnilca doliną wspomnianego Kojšovského potoku ( przez Veľký Folkmar ) .
Historia .
Powstał jako osada górników , poszukujących cennych kruszców , już z końcem XIII w. Został założony przez pana sąsiednich Jakloviec .
Pierwsze wzmianki o miejscowości pochodzą z roku 1368 .
