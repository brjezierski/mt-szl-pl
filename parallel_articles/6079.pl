Kokand
Kokand ( ; ) – miasto administrowane regionalnie w wilajecie fergańskim , we wschodnim Uzbekistanie , w południowo-zachodniej części Kotliny Fergańskiej .
Według szacunków z 2008 roku w Kokandzie mieszkało 216 700 osób .
Dawna stolica chanatu kokandzkiego .
Miasto jest oddalone o 88 km od stolicy regionu - Fergany .
Leży na skrzyżowaniu szlaków handlowych , uczęszczanych już w starożytności , przy zbiegu dwóch głównych dróg wiodących do Kotliny Fergańskiej : jednej prowadzącej na północny zachód do Taszkentu przez góry , a drugiej na zachód , przez Chodżent .
W efekcie Kokand stanowi główny węzeł transportowy Kotliny Fergańskiej .
Ośrodek przemysłu spożywczego , włókienniczego oraz chemicznego .
Historia .
Kokand istnieje przynajmniej od X wieku , znany wcześniej jako `` Chawakend '' , położony na szlaku handlowym z Chin do Indii .
W XIII wieku Kokand został zniszczony przez Mongołów .
Współczesne miasto powstało w 1732 roku jako twierdza zastępująca wcześniejszą fortecę o nazwie Eski-Kurgan .
W 1740 stało się stolicą chanatu kokandzkiego .
Było ono też głównym ośrodkiem religijnym Kotliny Fergańskiej ze swoimi ponad 300 meczetami .
Największy wpływ na miasto spośród przywódców chanatu miał Chudojar-Chan , dzięki któremu w mieście wybudowano wiele meczetów i medres oraz jego pałac , który można częściowo oglądać do dziś – pozostało jedynie 19 z 113 pokoi i dwa dziedzińce , które służą jako muzeum historii .
