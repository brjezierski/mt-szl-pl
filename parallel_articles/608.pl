20 ( album JWP i Bez Cenzury )
20 – trzeci mixtape polskich zespołów muzycznych JWP oraz Bez Cenzury .
Wydawnictwo ukazało się 20 stycznia 2017 roku nakładem wytwórni muzycznej RHW Records .
Album został wydany z okazji 20-lecia działalności grupy JWP .
W utworze `` Word Wide '' gościnnie wystąpiła pochodząca ze Stanów Zjednoczonych grupa Smif-n-Wessun , pojawili się Oni także w teledysku do tego utworu .
