Kokkola
Kokkola ( szw .
`` Karleby '' ) − miasto w Finlandii , nad Zatoką Botnicką .
Znajduje się w regionie Ostrobotnia Środkowa i podregionie Kokkola .
Jest ośrodkiem administracyjnym obu z nich .
Liczba mieszkańców w 2003 roku wynosiła ok. 35 tys .
W mieście znajduje się stacja kolejowa Kokkola .
W styczniu-kwietniu 1907 ukrywał się tu Włodzimierz Lenin .
