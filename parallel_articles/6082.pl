Kokoszkowy
Kokoszkowy – wieś kociewska w Polsce położona w województwie pomorskim , w powiecie starogardzkim , w gminie Starogard Gdański na Pojezierzu Starogardzkim przy drodze wojewódzkiej nr 222 .
W odległości 3 km od Starogardu Gdańskiego i ok. 43 km od Gdańska .
Miejscowość jest siedzibą rzymskokatolickiej parafii św .
Barbary należącej do dekanatu starogardzkiego .
W latach 1954–1972 wieś należała , a do 1959 była siedzibą władz gromady Kokoszkowy .
