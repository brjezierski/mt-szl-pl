Kogut ( symbolika )
Kogut ( także pod nazwami : `` kur '' , `` kurek '' , `` kokot '' ) – jako samiec kury domowej – w symbolice i ikonografii jest wieloznacznie interpretowany .
Symbolizujący nadejście światła , zwiastujący początek dnia , od czasów antycznych oznaczał czujność i przezwyciężenie ciemności .
Wówczas pojawiły się inne znaczenia – walczące koguty symbolizowały kłótnię i gniew .
Kogut w symbolice .
Obraz koguta w symbolice formował się dzięki naturalnym zachowaniom tych ptaków oraz na podstawie przypisywanych im cech .
W symbolice pojawił się już w Babilonii .
Indoariowie uznawali koguty za ptaki nieczyste i kojarzyli z Jamą .
W różnych mitologiach kogut budził bogów i sprowadzał słońce , tak jak na przykład Gullinkambi , obecny w mitologii nordyckiej .
W greckim micie o Alektryonie tytułowy bohater został zamieniony w koguta .
Był on stróżem Aresa i Afrodyty podczas ich zbliżeń , ponieważ bali się , że po nadejściu dnia Helios doniesie o zajściach Hefajstosowi .
Gdy Alektryon zaspał , za karę został przez Aresa zamieniony w koguta .
Kogut jest tu symbolem czujności i punktualności .
W starożytnej Grecji stanowił również symbol męskiej witalności .
Stanowi także atrybut Merkurego , opiekuna handlu .
