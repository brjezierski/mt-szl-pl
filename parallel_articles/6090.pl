Kolorado ( stan )
Kolorado ( , wym . )
– stan w środkowo-zachodniej części Stanów Zjednoczonych .
Na północy graniczy z Wyoming i Nebraską , na wschodzie z Kansas , na południu z Oklahomą i Nowym Meksykiem , a na zachodzie z Utah .
Kolorado jest jednym z trzech stanów USA niemających naturalnych granic .
Wyznaczają je : długość i szerokość geograficzna , a tym samym Kolorado ma kształt czworoboku .
Nazwa stanu pochodzi od nazwy rzeki Kolorado , która po hiszpańsku brzmi Rio Colorado .
Kolorado jest najwyżej położonym stanem USA ze średnią wysokością 2091 m n.p.m .
Największy obszar metropolitalny Denver–Aurora–Lakewood liczy blisko 3 mln mieszkańców i jest dziewiętnastym co do wielkości w Stanach Zjednoczonych .
Symbole stanu .
Kolorado zostało nazwane Stanem Stulecia , ponieważ w 1876 roku stało się 38 stanem , 100 lat po podpisaniu przez ojców założycieli Deklaracji Niepodległości .
Historia .
Pierwotnie ziemie tego regionu były zamieszkiwane przez Indian , którzy przybyli do Kolorado wraz z końcem epoki lodowcowej .
Najstarsze artefakty należące od pierwszych Indian datowane są na 11 200 lat p.n.e .
Na początku XVIII wieku tereny dzisiejszego stanu Kolorado zostały opanowane przez Hiszpanów .
W 1803 roku północno-wschodnia część dzisiejszego stanu została zakupiona od Francji , jako część francuskiej Luizjany .
W 1848 roku południowa i zachodnia część stała się terytorium Stanów Zjednoczonych po wojnie amerykańsko-meksykańskiej .
W roku 1858 odkryto złoża złota .
