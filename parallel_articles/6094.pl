Demografia .
Miasto zróżnicowane pod względem etnicznym i kulturowym .
Populacja Kolombo jest mieszanką wielu grup etnicznych , głównie Syngalezów , Murów i Tamilów .
Małe wspólnoty tworzą także : Chińczycy , Portugalczycy , Holendrzy i Malajowie .
