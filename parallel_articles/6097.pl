Kolonia Sielce
Kolonia Sielce – wieś w Polsce położona w województwie mazowieckim , w powiecie białobrzeskim , w gminie Stromiec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa radomskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Teresy od Dzieciątka Jezus w Dobieszynie .
