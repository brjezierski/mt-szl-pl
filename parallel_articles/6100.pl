Pod koniec XIV wieku powstał alfabet staropermski , który bazował na cyrylicy .
Tradycyjną wiarą wśród Komiaków był politeizm , jednak w XIV wieku ochrzczono ich i od tamtego czasu wyznają prawosławie , wśród nich znajdują się także staroobrzędowcy .
