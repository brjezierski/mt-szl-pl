Komiacy
Komiacy ( także „ Komi ” , „ Komi-Zyrianie ” , dawniej „ Zyrianie ” ; nazwa własna `` Komi '' , `` Komis '' ) – lud ugrofiński zamieszkujący głównie Republikę Komi ( Federacja Rosyjska ) , a także Półwysep Kolski i zachodnią Syberię .
Obszary te zamieszkuje 293–418 tys .
( w zależności przyjętego od kryterium narodowego — czytaj niżej ) Komiaków , posługujących się językiem komi i rosyjskim .
Wywodzą się ze średniowiecznych plemion ugrofińskich , zamieszkujących tereny między jeziorem Ładoga a Uralem .
W XIII i XIV w. ich ziemie włączone zostały do Republiki Nowogrodzkiej , a Komiacy podlegali silnym wpływom ruskim – wkrótce przyjęli również religię prawosławną .
Zachowali jednak elementy dawnego kultu przyrody wraz z rozwiniętym folklorem epickim i śpiewami chóralnymi .
Obok Komiaków istnieje druga grupa etniczna , także nosząca tę samą nazwę ( `` Komi '' ) , dla odróżnienia zatem w nauce określana jest mianem Komi-Permiaków .
Z tego też powodu Komiaków – określa się niekiedy Komi-Zyrianami .
Istnieją rozbieżne opinie na temat tego , czy obie te grupy to jeden naród , czy też dwa odrębne .
Rosyjskie spisy powszechne je rozróżniają , jednak sprawa ta nie jest ostatecznie rozstrzygnięta , także wśród samych Komiaków .
Nie jest także definitywnie rozstrzygnięte , czy używany przez te dwie grupy język komi to dwa dialekty tego samego języka , czy też odrębne języki .
Język ( lub dialekt ) komi używany przez Komiaków właściwych określa się mianem języka komi lub języka ( dialektu ) komi-zyriańskiego , zaś język ( dialekt ) Komi-Permiaków nazywany jest komi-permskim .
