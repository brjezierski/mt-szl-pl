Komsomolsk nad Amurem
Komsomolsk nad Amurem ( ) – miasto w Rosji , w Kraju Chabarowskim .
Port rzeczny na lewym brzegu Amuru , przy ujściu Silinki .
Duża stacja kolejowa na linii Wołoczajewka – Sowiecka Gawań , na 356 km od Chabarowska .
Drugie , pod względem liczby mieszkańców , miasto Kraju Chabarowskiego : 244 tys .
