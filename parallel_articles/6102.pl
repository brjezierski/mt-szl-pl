Historia .
Na miejscu wioski istniejącej od 1860 r. ( o nazwie Piermskoje , ze względu na to , że pierwsi osadnicy pochodzili z guberni permskiej ) i liczącej w 1932 r. 300 osób , w latach 1932–1933 zostało założone miasto , oficjalnie przez komsomolców – stąd nazwa , a praktycznie wzniesione przez łagierników .
Stało się dużym ośrodkiem przemysłowym rosyjskiego Dalekiego Wschodu .
Gospodarka .
Rozwinięty przemysł lotniczy , stoczniowy , metalurgiczny , odzieżowy , maszynowy , drzewny i naftowy .
Zakład przetwórstwa ropy naftowej działa dzięki surowcom dostarczanym z Sachalinu .
W mieście znajduje się w szczególności stocznia Amurskij Sudostroitielnyj Zawod ( dawna nr 199 ) , produkująca okręty i statki oraz Komsomolskie Zjednoczenie Przemysłu Lotniczego ( KnAAPO ) , produkujące samoloty Suchoja .
W mieście istnieje uczelnia medyczna i politechniczna .
W Komsomolsku działa sieć tramwajowa .
Religia .
Komsomolsk nad Amurem jest siedzibą eparchii amurskiej Rosyjskiego Kościoła Prawosławnego .
