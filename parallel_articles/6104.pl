Komárom
Komárom ( ) – miasto ( ponad 19,7 tys .
mieszkańców w I 2011 ) nad Dunajem w północnych Węgrzech , około 35 km na wschód od Győr .
Historia .
Miasto zostało w 1920 podzielone w wyniku traktatu w Trianon między Węgry i Czechosłowację ( słowacka obecnie część istnieje pod nazwą Komárno ) .
W mieście znajdują się zakłady przemysłowe ( Nokia , browar Dreher ) , kąpielisko termalne oraz trzy forty Twierdzy Komárno
