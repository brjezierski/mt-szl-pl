Kondopoga
Kondopoga ( ros .
`` Кондопога '' , karelski `` Kondupohju '' , fiń .
`` Kontupohja '' ) – miasto w północno-zachodniej części Federacji Rosyjskiej , w Republiki Karelii .
Położenie .
Miejscowość leży w południowej części Karelii , nad jeziorem Onega , w pobliżu miejsca , w którym wpada do niego rzeka Suna .
Kondopoga położona jest Rejonie kondopożskim i stanowi jego centrum administracyjne .
Historia .
Nazwa miasta jest pochodzenia karelskiego i może oznaczać niedźwiedzi ślad , niedźwiedzia łapa , albo też niedźwiedzi zalew .
Pierwsza wzmianka o miejscowości pochodzi z 1495 roku .
Przez długie lata była to niewielka osada .
W 1892 Kondopoga liczyła ok. 300 mieszkańców i znajdowało się w niej 48 domów i 2 cerkwie ( cerkiew Zaśnięcia Matki Bożej oraz cerkiew Narodzenia Matki Bożej ) .
Szybki rozwój miasta rozpoczął się w okresie ZSRR w związku z rozwojem w Karelii przemysłu i wynikającym z tego napływem rosyjskich osadników .
W 1938 roku miejscowość otrzymała prawa miejskie .
Ludność .
Kondopoga liczy 34.381 mieszkańców ( 1 stycznia 2005 r. ) .
Jest ona jedynym miastem na terenie rejonu kondopożskiego i skupia blisko 80 % ogółu ludności tej jednostki podziału administracyjnego .
Zdecydowaną większość populacji stanowią Rosjanie .
Liczba mieszkańców miasta w ostatnich latach dość szybko spada w wyniku emigracji ludności do większych miast ( głównie Petersburga ) w poszukiwaniu pracy .
Na przełomie sierpnia i września 2006 r. o Kondopodze było głośno w mediach światowych z powodu zamieszek do jakich doszło pomiędzy zamieszkującymi miasto Rosjanami a przybyszami z północnego Kaukazu , głównie Czeczenami .
W ich wyniku dwie osoby zginęły , a wiele zostało rannych .
Władze miasta utrzymywały , iż zajścia miały charakter chuligański a ich tłem był konflikt ekonomiczny , zaś organizacje obrony praw człowieka wskazywały na ich nacjonalistyczno-rasistowski charakter .
Gospodarka .
Po rozpadzie Związku Radzieckiego gospodarka miasta , jak i całej Karelii znalazła się w kryzysie .
Mimo to
Kondopoga jest jednym z centrów gospodarczych Karelii .
W mieście znajduje się przemysł drzewny , celulozowo-papierniczy oraz budowlany .
Z Kondopogi pochodzi ok. 1/3 całej rosyjskiej produkcji papieru gazetowego .
W miejscowości zlokalizowane są także zakłady przemysłu spożywczego , przetwarzające płody rolne , gdyż rejon kondopożski jest jednym z karelskich centrów rolnictwa .
Zatrudniają one zwykle niewielką liczbę osób i produkują głównie na potrzeby lokalnego rynku .
Istotne znaczenie w gospodarce miasta odgrywają szeroko rozumiane usługi .
