Kongsberg ( stacja kolejowa )
Kongsberg – kolejowy przystanek osobowy w Kongsbergu , w regionie Buskerud w Norwegii , jest oddalony od Oslo Sentralstasjon o 99,37 km .
Jest położony na wysokości 161,9 m n.p.m .
Ruch dalekobieżny .
Jest stacją obsługującą dalekobieżne połączenia z południowo-zachodnią i południową częścią kraju .
Stacja przyjmuje sześć par połączeń dziennie do Kristiansand , Arendal i Stavanger .
Ruch lokalny .
Leży na linii Sørlandsbanen .
Jest elementem kolei aglomeracyjnej w Oslo .
Jest ostatnią stacją linii 450 .
Stacja obsługuje Oslo Sentralstasjon , Drammen , Asker , Lillestrøm i Skøyen .
Pociągi linii 450 odjeżdżają w obu kierunkach co godzinę ; od Asker jadą trasą Askerbanen a między Oslo a Lillestrøm jadą trasą Gardermobanen .
Obsługa pasażerów .
Wiata , poczekalnia , kasa biletowa , automat biletowy , telefon publiczny , parking na 207 miejsc , parking rowerowy , winda peronowa , ułatwienia dla niepełnosprawnych , schowki bagażowe , kiosk , przystanek autobusowy , postój taksówek .
Blisko stacji narciarskie trasy zjazdowe .
