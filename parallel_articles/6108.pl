Kongsvinger ( stacja kolejowa )
Kongsvinger – stacja kolejowa w Kongsvinger , w regionie Hedmark w Norwegii , jest oddalona od Oslo Sentralstasjon o 100 km .
Położenie .
Jest końcową stacją towarowej linii Alnabru-Kongsvingerlinjen , jest położona na Kongsvingerbanen .
