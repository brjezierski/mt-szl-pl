Konstal 102Na
Konstal 102Na – normalnotorowy tramwaj wytwarzany w latach 1970–1973 przez zakłady Konstal w Chorzowie .
Wersja wąskotorowa ( początkowo oznaczona symbolem 102NaW , a później 803N ) produkowana była w latach 1973–1974 .
Konstrukcja .
Wagon typu 102Na jest zmodyfikowaną wersją tramwaju 102N , w której powrócono do kształtu ścian czołowych montowanych wcześniej w wozach 13N .
Jest to wagon przegubowy , dwuczłonowy , sześcioosiowy , przystosowany do eksploatacji pojedynczo oraz w dwuwagonowych składach .
Wagony 102Na są napędzane czterema silnikami umieszczonymi na pierwszym i trzecim wózku ( po 2 na wózek ) , natomiast wózek środkowy jest toczny .
Maksymalne przyspieszenie składu nieobciążonego wynosi 1 m/s² , a maksymalna prędkość to około 55 km/h .
Zastosowano rozruch oporowy .
Wersje .
Oprócz normalnotorowej podstawowej wersji 102Na produkowano także wersję wąskotorową 102NaW .
W dokumentacji fabrycznej opisana jest również odmiana 102Nb – charakteryzująca się brakiem gniazd sterowania ukrotnionego .
Dla Wrocławia wyprodukowano w roku 1973 wersję 102Nd , różniącą się przełączalnością silników trakcyjnych podczas rozruchu , nieprzystosowaniem do jazdy w trakcji ukrotnionej i drobnymi szczegółami konstrukcyjnymi ( m.in .
montaż wiązki przewodów elektrycznych na dachu oraz zastosowanie trójpłatowych drugich i trzecich drzwi ) .
Ostatni tramwaj tego typu został zezłomowany w lutym 2018 roku przez osobę prywatną , która odkupiła go za 5 tys .
