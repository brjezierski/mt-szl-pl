Konstal N
Konstal N – typ tramwaju wytwarzanego w latach 1948–1956 przez zakłady Konstal w Chorzowie , Stocznię Gdańską nr 3 i Sanocką Fabrykę Wagonów „ Sanowag ” .
Wersja wąskotorowa 2N produkowana była w latach 1950–1956 .
Wersje doczepne ( wagony bez silnika ) oznaczane są ND lub 2ND .
N to pierwszy tramwaj wyprodukowany w Polsce po II wojnie światowej .
Konstrukcja .
N to dwukierunkowy , dwustronny , jednoczłonowy wagon silnikowy , wyposażony w czworo jednoczęściowych , odsuwanych drzwi .
W powojennej Polsce wiele miast borykało się ze znacznymi brakami taborowymi i w związku z tym niezbędne było opracowanie tramwaju o prostej konstrukcji i łatwym montażu .
Postanowiono więc skopiować konstrukcję niemieckiego wagonu Kriegsstraßenbahnwagen ( KSW ) , opracowanego na potrzeby wojenne .
Ze względu na brak silników trakcyjnych pierwsze z taśm montażowych zjechały wagony doczepne ND przeznaczone dla Górnego Śląska .
Kolejną wersją był tramwaj 4N .
Sterowanie .
Wagon posiada sterowanie wyłącznie ręczne za pomocą nastawnika .
W nastawniku znajduje się obracany korbą motorniczego wał kułakowy , który zwierając poszczególne oporniki rozruchowe reguluje prąd rozruchu i tym samym przyśpieszenie wagonu .
Nastawnik posiada 23 pozycje rozruchowe jazdy .
Pozycje 1-13 są pozycjami oporowymi przy szeregowym połączeniu silników , pozycje 15-22 są pozycjami oporowymi przy połączeniu równoległym silników .
Pozycje 14 i 23 są pozycjami bezoporowymi- kolejno z szeregowym i równoległym połączeniu silników trakcyjnych .
Jazda na nich może trwać dowolnie długo .
Przy poz .
13 maksymalna prędkość wagonu to 35 km/h , przy poz .
23 – 55 km/h .
Silniki trakcyjne LT-31 o mocy 60 kW każdy .
Wagon posiada 16 pozycji hamowania elektrodynamicznego .
Podczas hamowania elektrodynamicznego ( służbowego ) z silnikami trakcyjnymi łączy się oporniki rozruchowe , w których na ciepło zostaje zamieniona SEM powstająca na zaciskach silników trakcyjnych .
Tempo przestawienia korby nastawnika na następne pozycje jest związane z szybkością przyśpieszania/hamowania wagonu .
Ponieważ przy niskich prędkościach jazdy hamulec elektrodynamiczny jest mało skuteczny- do hamowania wagonu używa się hamulca ręcznego ( klockowego ) .
Hamulec ten zapewnia też utrzymanie wagonu na postoju w miejscu spadku .
W wagonie doczepnym układ hamulcowy uruchamiany jest za pomocą elektromagnesu , przez który przepływa prąd wytwarzany w silnikach trakcyjnych wagonu silnikowego .
Elektromagnes przyciąga zworę połączoną z główną dźwignią układu hamulcowego .
Dźwignia ta jest połączona z układem klocków , które dociskane do kół powodują hamowanie .
Wagony typu N nie były fabrycznie wyposażane w hamulec szynowy .
Wiele egzemplarzy wagonów N miało ten hamulec zainstalowany dla skrócenia drogi hamowania awaryjnego .
Był on włączany na ostatnich pozycjach hamowania nastawnika , jak i odrębnym wyłącznikiem .
Hamulce montowano parami zawsze między osiami wozu .
Wersje .
Normalnotorowa wersja N produkowana była w trzech odmianach różniących się układem hamulcowym oraz szczegółami budowy .
Tramwaje wytwarzane były w Chorzowskiej Wytwórni Konstrukcji Stalowych „ KONSTAL ” , ale wiele elementów powstawało w innych zakładach , m.in .
w Fabryce Rohn-Zieliński w Żychlinie produkowano silniki trakcyjne , w Państwowej Fabryce Aparatów Elektrycznych w Łodzi wytwarzano nastawniki , automaty , solenoidy i opory rozruchowe , a w pewnym okresie produkcję silników prowadziły wrocławskie zakłady Dolmel .
Tramwajów silnikowych N wyprodukowano łącznie 516 sztuk .
Wersja N1 , produkowana w zakładach Konstal w latach 1949–1950 , była w zasadzie kopią wagonu KSW ( dokonano m.in .
uproszczenia układu elektrycznego ) , choć również zawierała elementy rozwojowe – jak niewielkie uchylne okna .
W wersjach N2 ( produkowana w Konstalu w latach 1950–1952 i w Stoczni Gdańskiej w latach 1950–1951 ) i N3 ( Konstal 1952-1956 ) systematycznie stosowano rozmaite ulepszenia , między innymi układu hamulcowego , wprowadzenie otwieranych okien i zwiększano udział krajowych podzespołów .
Produkcję normalnotorowych wagonów doczepnych ND1 prowadzono w Konstalu w latach 1948–1950 i w Sanoku w latach 1949–1951 .
Wersja ND2 produkowana była w Konstalu w latach 1950–1953 i w Sanoku w latach 1951–1952 .
Odmiana ND3 produkowana była już wyłącznie przez zakłady Konstal w latach 1953–1956 .
Ogółem taśmy produkcyjne opuściło około 642 wagonów doczepnych ND .
Wersja wąskotorowa wagonu silnikowego 2N1 produkowana była w Stoczni Gdańskiej w latach 1950–1951 w liczbie 50 sztuk .
Kolejną wersję 2N2 budowały już zakłady Konstal i w latach 1952–1956 wyprodukowały 148 sztuk tych wagonów .
Wersja doczepna 2ND została wyprodukowana w roku 1955 w liczbie 15 sztuk .
Większość użytkowników ( jak np .
na Górnym Śląsku ) nie stosowała powszechnie wyróżników liczbowych , w dokumentacji oznaczając wagony jako jednakowy typ N .
Eksploatacja .
Tramwaje normalnotorowe typu N eksploatowane były przez większość polskich miast posiadających wówczas komunikację tramwajową : Gdańsk , Gorzów Wlkp. , Sopot , GOP , Kraków , Poznań , Szczecin , Warszawa , Wrocław , zaś wersje wąskotorowe używane były m.in .
w Bielsku-Białej , Bydgoszczy , Grudziądzu , Elblągu , Inowrocławiu , Jeleniej Górze , Legnicy , Łodzi i sąsiadujących miejscowościach , Olsztynie , Słupsku , Toruniu i Wałbrzychu .
Przez cały okres eksploatacji poddawane one były różnym ulepszeniom i modernizacjom .
Do 2020 roku 2 egzemplarze eksploatowane były liniowo w Bytomiu , gdzie obsługiwały jednotorowy odcinek linii 38 bez pętli końcowej na ulicy Piekarskiej .
Są to wagony 954 z 1949 i 1118 z 1951 roku .
W pozostałych miastach wagony N używane są w charakterze wozów technicznych , a część miast podjęła się remontów tych wagonów i obecnie zasilają one zbiory taboru historycznego ( w tym jedyny w pełni oryginalny wagon ND numer 538 w Krakowie ) .
Tramwaje typu N są najdłużej eksploatowanymi tramwajami w Polsce po II wojnie światowej .
