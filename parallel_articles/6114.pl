Konstancja ( imię )
Konstancja – żeńska forma imienia Konstancjusz , wywodzącego się od wyrazu pospolitego `` constans '' `` niezmienny , stały '' .
Imię zapisano w Polsce już w XIII wieku , nosiły je m.in .
księżniczki piastowskie : córka Henryka II Pobożnego , córka Przemysła I , a także córka Bernarda Świdnickiego .
Konstancja imieniny obchodzi : 18 lutego , 14 czerwca , 17 lipca i 19 września .
