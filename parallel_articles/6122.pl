Komarowate
Komarowate , komary ( Culicidae ) – występująca na całym świecie rodzina owadów ( nadrodzina Culicoidea ) z rzędu muchówek .
Znanych jest ponad 40 rodzajów i ok. 3,5 tys .
gatunków komarów .
Dawniej do rodziny komarowatych zaliczano także jako podrodziny : Chaoborinae ( obecnie rodzina Chaoboridae ) i Dixinae ( obecnie rodzina Dixidae ) .
Budowa ciała .
Dorosłe komary mają aparat gębowy kłująco-ssący .
Składa się on z wydłużonych : warg ( górnej i dolnej ) , żuwaczki , podgębia oraz szczęk I pary .
Fizjologia .
Samice tych owadów ssą krew zwierząt stałocieplnych , a niektóre owadów , ponieważ do powstania jaj w jajnikach wymagają odżywiania się krwią określonych gatunków zwierząt .
Natomiast samce żywią się nektarem kwiatów .
Samice komarów przywabia zwiększone stężenie dwutlenku węgla w wydychanym powietrzu oraz kwas mlekowy i inne składniki potu , takie jak oktanol .
Posługują się też termodetekcją , co sprawia , że w przypadku ludzi bardziej narażeni na ukąszenia komarów są młodzi mężczyźni , dzieci i kobiety w okresie owulacji .
Badania wykazały , że receptory `` Anopheles gambiae '' ( wprowadzony do mutanta muszki owocowej ) reagują na 4-metylofenol oraz 1-okten-3-ol .
Jeden rodzaj receptora reaguje na jeden lub niewielką liczbę związków chemicznych .
Samica po przyłożeniu kłujki do ciała zwierzęcia puszcza w ruch szczęki kształtu lancetu zaopatrzonego w haczyki .
Ruchy szczęk w górę i w dół powodują przecięcie skóry , następnie do rany zostaje wstrzyknięta kropelka śliny , która ma zapobiec krzepnięciu krwi .
Samica odlatuje dopiero wtedy , gdy wypełni wole krwią ofiary .
Ślina samicy zawiera substancję chemiczną , podrażniającą skórę , może także dochodzić do reakcji alergicznej .
Dlatego po ukłuciu skóra czerwienieje , puchnie i swędzi .
Tryb życia i cykl rozwojowy .
Larwy rozwijają się w środowisku wodnym .
Osobniki dorosłe latają wieczorami w dużych chmarach nad podmokłymi łąkami , w pobliżu jezior , stawów i rzek .
Jedne gatunki wybierają zacienione przybrzeża , inne znowu oświetlone , wolne przestrzenie wodne .
Komarowate przechodzą przeobrażenie zupełne .
Samica , po nassaniu się krwi ssaka , składa na powierzchni wody zwężone na jednym końcu jaja tak blisko siebie , że woda nie może dostać się między nie .
W niektórych przypadkach jaja nie rozwijają się , o ile nie przejdą przez okres chłodu i nie znajdują się przez pewien okres w środowisku wilgotnym .
Z jaj wylęgają się larwy .
Oddychają one wystawiając ponad powierzchnię wody specjalną rurkę , w którą zaopatrzony jest odwłok .
Położenie , jakie zajmują larwy w wodzie , jest charakterystyczne dla danego gatunku .
