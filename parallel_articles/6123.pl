Kopydłów
Kopydłów – wieś w Polsce , położona w województwie łódzkim , w powiecie wieluńskim , w gminie Biała .
W latach 1975–1998 wieś administracyjnie należała do województwa sieradzkiego .
Historia .
Wieś wzmiankowana w 1595 roku , początkowo nazywała się Nabwisdorf ( założycielami byli prawdopodobnie niemieccy osadnicy ) .
Od 1409 istniała pod nazwą Kopidlow .
W 1337 roku mieszkało we wsi 143 osób w 17 domach , w 1883 mieszkańców było 254 , a domów 22 .
Czasy obecne .
Miejscowość była pierwowzorem fikcyjnego Kopydłowa , gdzie toczyła się akcja telewizyjnego programu rozrywkowego `` Spotkanie z Balladą '' .
W Kopydłowie urodził się Leszek Benke , aktor .
Urodził się tam także piłkarz Kacper Panek .
