Kopenhaga
Kopenhaga ( duń .
`` København '' , łac .
`` Hafnia '' ) – stolica i największe miasto Danii położone na wschodnim wybrzeżu wyspy Zelandia i częściowo Amager .
Od 1 lipca 2000 połączona jest mostem nad Sundem ze szwedzkim Malmö .
Obecnie zamieszkuje ją 794 128 osób ( 2020 ) , a cały zespół miejski tzw .
„ Wielkiej Kopenhagi ” liczy 1 330 993 mieszkańców ( stan na 2020 rok ) .
Klimat .
W Kopenhadze panuje klimat morski .
Przejawia się to częstymi opadami oraz dużą wilgotnością powietrza .
Najcieplejsze miesiące to lipiec i sierpień , lecz są one też najbardziej deszczowe .
Roczne amplitudy temperatur są niskie – oznacza to , że zimy są łagodne , a lata chłodne .
Historia .
Średniowiecze .
Kopenhaga nie należy do najstarszych miast Danii , chociaż ślady ludzkiej egzystencji na tym miejscu pochodzą z epoki kamiennej ( ok. 10 000 lat przed n.e . ) .
W czasach wikingów istniało tutaj targowisko , ale miasto założył dopiero w 1167 r. biskup Roskilde Absalon , który otrzymał tę część Zelandii z nadania króla Waldemara I w poł .
XII w. Z inicjatywy Absalona wzniesiono gród warowny , Slotsholmen , na wyspie naprzeciw niewielkiej osady rybackiej zwanej `` Havn '' ( port ) .
W 1191 r. Absalon zapisem testamentowym przekazał Kopenhagę na własność biskupom Roskilde .
W `` Księdze Ziemskiej '' króla Waldemara I ( `` Kong Valdemars Jordebog '' ) z 1231 r. nazwana została `` Kiopmanhafn '' ( kupiecki port ) .
Miejsce założenia Kopenhagi nie było przypadkowe .
Leżało bowiem w połowie drogi pomiędzy ówcześnie ważnymi ośrodkami władzy królewskiej w Danii , Roskilde i Lund , i było miejscem krzyżowania się szlaków handlowych : lądowego z Zelandii do Skanii i morskiego , z Bałtyku w kierunku Morza Północnego przez Sund .
Strategiczna rola tego miejsca miała znaczenie pierwszoplanowe dla Absalona przy podjęciu decyzji o założeniu miasta właśnie w tym miejscu .
Wokół grodu warownego wyrosło z czasem średniowieczne miasto , które w 1209 r. otrzymało kościół NMP , dzisiejszą katedrę .
W 1254 r. Kopenhaga otrzymała prawa miejskie .
