Kora ( sztuka )
Kora ( `` kore '' – dziewczyna ; lm .
`` korai '' ) – w sztuce Grecji okresu archaicznego najstarszy typ greckiego posągu kobiecego , wyobrażający stojącą dziewczynę w długich szatach , często z owocem , kwiatem lub ptakiem w ręku ; żeński odpowiednik kurosa .
Cechy charakterystyczne .
Rzeźba wykonywana była z wapienia , marmuru lub porosu .
Artyści starannie opracowywali szczegóły stroju i uczesania , które podkreślali polichromowaniem , a wyraz twarzy przedstawiali z charakterystycznym archaicznym uśmiechem .
Posągi te miały prawdopodobnie charakter portretowy , ich cechy fizjonomii , stroje , uczesanie są zróżnicowane .
Kory były darami wotywnymi składanymi w świątyniach .
Greczynki dedykowały je m.in .
bogini Herze lub Atenie .
Wyjątkowo miały przeznaczenie sepulkralne .
W budowlach jońskich pełniły funkcję podpór architektonicznych tzw .
kariatyd , zastępując kolumny np .
w skarbcu Syfnijczyków w Delfach lub w ganku Erechtejonu w Atenach .
