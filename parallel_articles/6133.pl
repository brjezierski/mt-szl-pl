Dynia ozdobna
Dynia ozdobna – nazwa zwyczajowa różnych gatunków i odmian dyni uprawianych dla ozdobnych owoców .
Przeważnie są to różne odmiany dyni zwyczajnej ( `` Cucurbita pepo '' ) lub dyni olbrzymiej ( `` Cucurbita maxima '' ) .
W handlu pod pojęciem dyni ozdobnej spotkać także można różne odmiany tykwy ( `` Lagenaria '' ) .
To typowe , jednoroczne rośliny pnące , które tworzą wielobarwne owoce o rozmaitych kształtach , nie nadające się do spożycia .
Owoce , zebrane we wrześniu lub październiku , wysuszone w suchym i przewiewnym miejscu mają walory dekoracyjne .
Spotykane są różne odmiany różniące się kształtem i kolorami ( np .
korona cierniowa , dynia butelkowa , sułtański turban ) .
