Korczyna ( województwo podkarpackie )
Korczyna – wieś w Polsce , położona w województwie podkarpackim , w powiecie krośnieńskim , w gminie Korczyna .
Leży ok. 5 km na północny wschód od Krosna .
Miejscowość jest siedzibą gminy Korczyna .
Korczyna uzyskała lokację miejską w 1516 roku , ale nie została ona zrealizowana , ponowna lokacja w 1785 .
W latach 1878–1934 Korczyna posiadała prawa miejskie .
W latach 1954–1972 wieś należała i była siedzibą władz gromady Korczyna .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krośnieńskiego .
