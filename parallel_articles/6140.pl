Kornel Morawiecki
Kornel Andrzej Morawiecki ( ur .
3 maja 1941 w Warszawie , zm .
30 września 2019 tamże ) – polski polityk , fizyk i nauczyciel akademicki .
Działacz opozycji demokratycznej w czasach PRL , założyciel i przewodniczący Solidarności Walczącej , poseł i marszałek senior Sejmu VIII kadencji .
Kandydat na urząd prezydenta RP w wyborach w 2010 .
Kawaler Orderu Orła Białego .
Ojciec Mateusza Morawieckiego , prezesa Rady Ministrów .
Życiorys .
Wykształcenie i działalność zawodowa .
Syn Michała ( 1914–1977 ) i Jadwigi z domu Szumańskiej ( 1912–1975 ) .
Maturę zdał w 1958 w IV Liceum Ogólnokształcącym im .
Adama Mickiewicza w Warszawie .
W 1963 ukończył studia na Wydziale Fizyki Uniwersytetu Wrocławskiego .
W 1970 uzyskał stopień naukowy doktora na podstawie pracy poświęconej kwantowej teorii pola napisanej pod kierunkiem profesora Jana Rzewuskiego .
Był pracownikiem naukowym na Uniwersytecie Wrocławskim , początkowo w Instytucie Fizyki Teoretycznej , następnie w Instytucie Matematycznym .
Od 1973 do połowy 2009 pracował w Instytucie Matematyki i Informatyki Politechniki Wrocławskiej .
Działalność opozycyjna w okresie PRL .
W 1968 uczestniczył w strajkach studenckich .
Po ich stłumieniu , wspólnie z Jerzym Petryniakiem , Zdzisławem Ojrzyńskim , Piotrem Plenkiewiczem oraz Ryszardem Trąbskim , wydrukował i kolportował ulotki potępiające ówczesne władze komunistyczne za represje wobec protestujących .
W podobnym zespole wydawał ulotki przeciwko interwencji wojsk Układu Warszawskiego w Czechosłowacji w sierpniu 1968 , a następnie przeciwko stłumieniu przez władze protestów robotniczych w grudniu 1970 .
