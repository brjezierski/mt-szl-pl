Kornowac
Kornowac ( niem .
`` Kornowatz '' ) – wieś w Polsce położona w województwie śląskim , w powiecie raciborskim , w gminie Kornowac .
W latach 1975–1998 miejscowość administracyjnie należała do województwa katowickiego .
Miejscowość jest siedzibą gminy Kornowac .
Wieś w liczbach .
Według Narodowego Spisu Powszechnego Ludności i Mieszkań z 2021 roku liczba ludności we wsi Kornowac to 895 .
50,6 % mieszkańców stanowią kobiety , a 49,4 % ludności to mężczyźni .
Miejscowość zamieszkuje 17,3 % mieszkańców gminy .
Geografia .
Miejscowość jest położona wysoko nad doliną Odry .
Historia .
Nazwa miejscowości , według podania wywodzi się z imiona Garno i Vac – braci , którzy tutaj osiedlili , lub od słowa koronaciec – czyli w kształcie korony .
Wzmiankowana około roku 1300 .
W 1308 roku jako `` Coronovatiz '' oraz `` Wurndorf '' nadana przez księcia Leszka w posiadanie dziedziczne klasztorowi dominikanek w Raciborzu .
W okresie międzywojennym w miejscowości stacjonował komisariat Straży Granicznej i placówka II linii SG „ Kornowac ” .
