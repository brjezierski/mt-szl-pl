Inne symbole .
Flagą Księcia Kornwalii jest czarny prostokąt z trójkątem równobocznym ułożonym z czternastu złotych monet tle , zwróconym wierzchołkiem w dół .
Metale , zwłaszcza cyna i miedź , są często wykorzystywane w symbolice Kornwalii z racji ich znaczenia dla gospodarki regionu .
Symbolem Kornwalii jest wrończyk ( `` Pyrrhocorax pyrrhocorax '' ) – w poezji kornwalijskiej używa się go jako symbolu regionu .
W Kornwalii wierzono również , że król Artur żyje obecnie pod postacią wrończyka .
Jego angielska nazwa `` chough '' często służyła w Anglii jako przydomek Kornwalijczyka .
Wizerunek ptaka znajduje się w herbie Kornwalii .
Ptak , ongiś popularny w Kornwalii , obecnie na większą skalę żyje tylko w Irlandii .
Choć Kornwalia nie ma oficjalnego symbolu kwiatowego , jego rolę pełni często wrzosiec rozpierzchły ( `` Erica vagans '' L. ) oraz żonkil , eksponowany głównie w dniu św .
Pirana .
Kowadło jest symbolem kornwalijskich kowali i oznacza honor .
Jest to również odwołanie do Michaela An Gof ( pol .
Kowalski ) , przywódcy rebelii z r. 1497 .
Język kornijski .
Język kornijski należy do grupy języków celtyckich i jest blisko spokrewniony z walijskim i bretońskim , mniej z irlandzkim , szkockim gaelickim i manx .
Kornijski funkcjonował jako język społeczności do końca XVIII wieku .
W roku 1904 został opublikowany „ Podręcznik języka kornijskiego ” autorstwa Henry ’ ego Jennera .
Badania z roku 2000 wykazały , że językiem kornijskim posługuje się płynnie ok. 300 osób , a 3000 użytkowników jest w stanie przeprowadzić prostą rozmowę w tym języku .
W Wielkiej Brytanii język jednak nie ma określonego statusu prawnego , niemniej jednak naucza się go w ok. dwunastu szkołach podstawowych .
Używany jest okazjonalnie przy uroczystościach świeckich i religijnych .
W roku 2002 język kornijski został oficjalnie uznany za język mniejszości narodowej w Wielkiej Brytanii , a w roku 2005 lokalne władze otrzymały dotacje od rządu na jego promocję i ochronę .
Standardową formę języka przyjęto w roku 2008 .
Dwóch członków Parlamentu Wielkiej Brytanii , Andrew George , deputowany z St Ives i Dan Rogerson , reprezentujący Kornwalię Północną , złożyło przysięgi parlamentarne po kornijsku .
Geografia .
Linia brzegowa .
Hrabstwo Kornwalia zajmuje czubek półwyspu położonego na południowym zachodzie wyspy Wielka Brytania i dlatego narażone jest na działanie silnych wiatrów wiejących od Oceanu Atlantyckiego .
Północne i południowe wybrzeże półwyspu różnią się znacząco .
Północne wybrzeże , leżące bezpośrednio nad Atlantykiem , ma charakter klifowy – tu właśnie , między miejscowościami Boscastle i Tintagel znajduje się High Cliff , najwyższy klif w Kornwalii ( 224 m ) .
Jednocześnie są tu również długie piaszczyste plaże , będące atrakcją turystyczną , m.n .
w Bude , St Ives czy też Newquay .
W tej części wybrzeża swe ujście znajdują dwie rzeki : Hayle oraz Camel , która stwarza warunki do istnienia bezpiecznej przystani w Padstow .
Wybrzeże południa Kornwalii , zwane `` rivierą '' , jest bardziej osłonięte , toteż są tam lepsze warunki dla bezpiecznych przystani w ujściach rzek , m.in .
miasta Falmouth i Fowey .
Plaże na południu pokrywa gruboziarnisty piasek , gdzieniegdzie spotyka się też skaliste platformy abrazyjne .
Ukształtowanie powierzchni i budowa geologiczna .
Wnętrze Półwyspu Kornwalijskiego składa się z pasów wyżyn rozciągniętych w kierunku równoleżnikowym .
Wyżyny te tworzą masywy granitowe .
Przykładem jest płaskowyż Bodmin Moor , na którym znajduje się Brown Willy – najwyżej położony punkt Kornwalii .
Następnie ( ze wschodu na zachód ) pojawia się wywyższenie wokół St Austell , Camborne , półwysep Penwith i Land ’ s End .
Z intruzywnych granitów zbudowane są także Wyspy Scilly ( obecnie częściowo zatopione ) oraz wzgórza na obszarach graniczących z Kornwalią , takich jak Dartmoor .
W strefach kontaktowych między masywami granitowymi a osadowymi skałami osłony tychże masywów miały miejsce intensywne procesy metamorfizmu kontaktowego i mineralizacji .
Zjawiskom tym Kornwalia zawdzięcza posiadanie rud metali kolorowych : cyny , miedzi , ołowiu i cynku , które do XX wieku miały dla regionu kluczowe znaczenie gospodarcze .
Proces wietrzenia granitu spowodował również powstanie złóż kaolinu – glinki używanej jako surowiec do produkcji porcelany , zwłaszcza w okolicy St Austell , które do dziś jest ważnym centrum przemysłu ceramicznego .
Granitowy płaskowyż otoczony jest przez bardziej żyzne grunty pastwiskowe .
Przy południowym wybrzeżu zadrzewione doliny stwarzają dobre warunki do wegetacji roślinności lubiącej klimat ciepły i wilgotny .
Teren ten położony jest głównie na warstwach piaskowca i łupków .
Północny wschód Kornwalii leży na skałach pochodzących z karbonu , zwanych inaczej `` Culm Measures '' .
Będący częścią Półwyspu Kornwalijskiego półwysep Lizard przejawia dużą odmienność w porównaniu z resztą kraju .
Jest to jedyny na terenie Wielkiej Brytanii obszar występowania ofiolitu – kompleksu skał magmowych uformowanego pod powierzchnią oceanu .
Część ultrazasadowych skał ofiolitu została zmetamorfizowana i obecnie występuje jako powstałe w prekambrze serpentynity , głównie ciemnozielone i czerwone .
Tworzą one widowiskowe klify ; małe odłamki serpentynu są sprzedawane w sklepach z pamiątkami .
Wnętrze półwyspu pokrywają mało żyzne gleby o niewielkiej miąższości , leżące przeważnie bezpośrednio na podłożu zbudowanym ze skał ultrazasadowych .
Obszar ten jest dość jałowy , porastają go najczęściej wrzosowiska .
Klimat .
Kornwalia zajmuje najbardziej na południe wysuniętą część Wielkiej Brytanii , dlatego jej klimat jest stosunkowo ciepły i słoneczny .
Przeważają łagodne zimy , a mróz i śnieg są zjawiskiem bardzo rzadkim , z wyjątkiem wyżyny w środku Półwyspu Kornwalijskiego .
Średnia temperatura roczna dla większości Kornwalii wynosi między 9,8 a 12 °C , i jest nieco niższa dla obszarów położonych wyżej .
Kornwalia znajduje się pod wpływem łagodnych wiatrów , przeważnie zachodnich z Oceanu Atlantyckiego i jest stosunkowo deszczowa , choć mniej niż bardziej na północ wysunięte obszary zachodniego wybrzeża Wielkiej Brytanii , od 1051 do 1290 mm w skali rocznej .
Na większości terytorium hrabstwa nasłonecznienie wynosi ponad 1541 godzin w roku .
Niniejszy wykres przedstawia temperatury minimalne i maksymalne oraz sumę opadów w Mawgan , południowo-zachodniej części hrabstwa .
Środowisko naturalne .
Fauna i flora .
Niewielkie uprzemysłowienie terenu , jak również jego niezdatność do intensywnego rolnictwa , zwłaszcza w głębi półwyspu spowodowały , że region ten wyróżnia się dobrze zachowaną różnorodnością gatunkową flory i fauny .
W regionie wypas bydła nigdy nie był intensywny i dużą rolę w kształtowaniu krajobrazów otwartych odegrały naturalne pożary .
W efekcie powstały korzystne warunki do rozwoju wrzosowisk z przewagą wrzosu , różnych gatunków wrzośców , w tym endemitu – wrzośca rozpierzchłego , rosnącego tylko na Półwyspie Lizard .
Na wrzosowiskach rosną poza tym borówki czarne ( lokalnie zwane `` whortleberry '' ) i kolcolisty .
Na wrzosowiskach spotyka się żmije zygzakowate – jedyny jadowity gatunek węża w Wielkiej Brytanii .
Liczne sztolnie po kopalniach stanowią dobre warunki do rozwoju fauny nietoperzy .
W miejscach podmokłych , w tym w zbiornikach po odkrywkowych kopalniach gliny , spotyka się żaby , ropuchowate i ważki różnoskrzydłe .
Obok krajobrazów mniej lub bardziej typowych dla regionu , stworzone tu zostały ogrody sztucznie urządzone w celu zgromadzenia flory i fauny subtropikalnej , zwłaszcza motyli i ptaków .
Miejscem wyjątkowym pod tym względem jest `` Tehidy Park '' niedaleko Camborne .
Stan środowiska naturalnego .
Środowisko naturalne w Kornwalii , jedno z naturalnych i ważnych bogactw zważywszy na rolę turystyki dla gospodarki regionalnej , narażone jest na liczne czynniki destrukcyjne , wynikające zarówno z tendencji ogólnoświatowych , np .
globalne ocieplenie , jak i typowo lokalnych , związanych z położeniem geograficznym .
Typowe dla tego regionu są powodzie – Agencja Środowiska ocenia , że w regionie South West England na powódź narażonych jest 100 000 gospodarstw .
W Kornwalii szczególnie narażona jest dolina rzeki Tamar , podczas powodzi 2000-2001 ucierpiały 63 domostwa .
W możliwym do przewidzenia czasie regionowi nie zagrażają niebezpieczeństwa związane z podnoszeniem się poziomu morza – od roku 1915 poziom podniósł się o 17 cm .
Położenie w pobliżu Atlantyku i małe uprzemysłowienie sprawia , że powietrze w Kornwalii jest czyste , z wyjątkiem większych miast , gdzie charakterystyczna dla Kornwalii ciasna zabudowa ulic sprzyja gromadzeniu się zanieczyszczeń , powstających w wyniku transportu drogowego .
Jakość wody w rzekach oceniana jest na bardzo dobrą i stale się polepsza : w 1992 stan czystości wód oceniano na 82 % a w roku 2001 – na 92 % .
Wody wszystkich rzek na każdym odcinku są co najmniej dobrej jakości .
Niemniej jednak obserwuje się spadek zarybienia rzek : w roku 1994 z wód Kornwalii wyłowiono 4000 łososi , w roku 2001 tylko 500 .
Pozytywne tendencje występują również w stanie czystości wód w kąpieliskach nadmorskich , a stan czystości wód w nich wynosił w roku 2001 79 % .
Na terenie hrabstwa znajdują się trzy rezerwaty przyrody rangi krajowej ( 0.58 % powierzchni hrabstwa ) i sześć lokalnych .
Ponadto są tu 63 inne obszary chronionej przyrody i krajobrazu , 103 stanowiska geologiczne jak i 144 chronione obszary o znaczeniu naukowym .
Historia Kornwalii .
Czasy prehistoryczne .
Pierwsze udokumentowane ślady obecności ludzkiej , w postaci kamiennych narzędzi , pochodzą z okresu 400 000-200 000 lat p.n.e .
Nie ma jednak dowodów na osadnictwo ludzi na terenie Kornwalii w tym czasie .
Pierwsze odkrycia sugerujące zamieszkanie tych obszarów pochodzą z okresu po ostatniej epoce lodowcowej , około 10 000 p.n.e .
i są to osady myśliwych , zlokalizowane głównie na półwyspie Lizard oraz na wyżynach ( m.in .
Bodmin Moor ) .
W okresie epoki neolitu nastąpił gwałtowny rozwój osadnictwa , powstały pierwsze struktury megalityczne .
Kornwalia i sąsiedni Devon posiadały duże złoża cyny , która wydobywana była już w epoce brązu przez ludzi z kultury pucharów dzwonowatych .
Ponieważ cyna niezbędna jest do produkcji brązu , około roku 1600 p.n.e .
region ten przeżył okres prosperity i był ośrodkiem eksportowym cyny do innych części Europy .
Z epoki brązu pochodzą menhiry i kamienne kręgi , obecne zwłaszcza na końcu półwyspu .
Epoka żelaza rozpoczęła się na Wyspach Brytyjskich około roku 600 p.n.e. , niosąc ze sobą rozwój rolnictwa , żelaznych pługów i siekier .
Nasiliła się również budowa grodzisk na wzgórzach .
Mniej więcej w tym samym czasie ( 900 – 500 p.n.e . )
plemiona celtyckie opanowały wyspę Wielką Brytanię .
Pierwsza wzmianka o Kornwalii pochodzi od greckiego historyka Diodora Sycylijskiego , a region ten nosi w jego pismach nazwę `` Belerion '' .
Epoka rzymska .
W okresie dominacji rzymskiej Kornwalia pozostawała na uboczu głównych centrów zainteresowania Rzymian .
Co prawda sięgał tu rzymski system dróg , jednak docierał tylko do dwóch twierdz : `` Restormel Castle '' i `` Nanstalon '' , jak też do rzymskiej willi `` Magor Farm '' niedaleko Camborne .
Skończył się handel cyną , wydobycie na skalę międzynarodową zastąpił import z Półwyspu Iberyjskiego .
Kornwalia jednak nie straciła łączności z sąsiednimi terytoriami dzięki uczęszczanym szlakom morskim z Padstow i Lostwithiel .
W przededniu wycofania Rzymian z Brytanii , Anglosasi byli w stanie podbić większość wyspy , Kornwalia pozostała jednak we władaniu elit rzymskich i celtyckich .
Kornwalia należała do plemienia „ Dumnonii ” , którego centrum znajdowało się na terenie współczesnego hrabstwa Devon .
Kornwalijska część zwana była „ Cornovii ” .
W okresie rzymskim podział między oboma plemionami nie był wyraźny a obie nazwy były stosowane wymiennie .
Do VIII wieku władcy Dumdonii byli jednocześnie władcami Cornovii .
W legendzie o królu Arturze Gorlois posiada tytuł księcia Kornwalii , ale nie ma dowodów na jego istnienie .
Okres ten występuje w Kornwalii jako “ wiek świętych ” – powstanie celtyckiego chrześcijaństwa i odrodzenie sztuki celtyckiej w Irlandii i Szkocji , która następnie zawędrowała do Kornwalii .
W tym właśnie okresie działali kornwalijscy święci : święty Piran , Meriasek czy Geraint ; często silne były ich związki z królami Kornwalii .
Średniowiecze .
Królestwo Kornwalii powstało ok. VI wieku , jego królowie początkowo podlegali brytyjsko-celtyckiemu Królestwu Dumnonii .
Sytuacja polityczna była niestabilna i niektórzy królowie kornwalijscy próbowali podporządkować sobie Bretanię .
Tymczasem ze wschodu nadciągało saksońskie zagrożenie z Wessexu , które doprowadziło do upadku królestwa Dumnonii .
W roku 721 Brytowie pokonali Saksonów pod “ Hehil ” .
Po wieku spokoju zachodni Saksonowie znów zaatakowali Kornwalię .
Kronika anglosaska informuje o bitwie pod Gafulforda , najprawdopodobniej pod obecnym Galford .
W roku 838 Kornwalijczycy wraz z Duńczykami zostali pokonani przez Egberta z Wessexu pod Hingston Down .
Do roku 880 w kościele kornwalijskim wykształciła się przewaga duchownych saksońskich .
Kontrolowali oni dobra kościoła , które przeszły ostatecznie na własność królów Wesseksu .
Jednak , zgodnie z opinią Alfreda Wielkiego , ilość ziemi posiadanej w Kornwalii była niewielka .
W roku 1013 Wessex został pokonany przez wojska duńskie pod dowództwem króla Danii Swena Widłobrodego .
Wessex został włączony do imperium duńskiego , jednak nie anektowano doń Kornwalii , Walii ani Szkocji , dając im autonomię w zamian za trybut .
Zapisy w Domesday Book świadczą , że do roku 1086 kornwalijscy właściciele zostali prawie całkowicie wywłaszczeni i zastąpieni przez właścicieli angielskich .
Dominował jednak język kornwalijski , zwłaszcza w zachodniej i środkowej części Kornwalii ; pierwsze typowo angielskie nazwy miejsc pojawiły się na zachód od rzeki Tamar dopiero około roku 1040 .
Organizacja Kościoła w Kornwalii w tamtych czasach nie jest jasna .
W połowie IX wieku kierował nim biskup Kenstec , który wprowadził Kościół kornwalijski pod jurysdykcję arcybiskupa Canterbury .
Pierwsi biskupi byli narodowości kornwalijskiej , od roku 963 wszyscy byli Anglikami .
Od 1050 roku Kościół kornwalijski został wchłonięty przez diecezję w Exeter .
Po roku 1066 większość ziemi w Kornwalii przeszła pod władanie nowej arystokracji , przy czym lwia część należała do Roberta , brata przyrodniego Wilhelma Zdobywcy , największego właściciela ziemi w Anglii po królu .
Przy licznych okazjach powoływano książąt Kornwalii , ich linie dynastycznie nie trwały jednak zbyt długo .
W średniowieczu rozwinęła się literatura popularna pisana w języku kornijskim , szczególnie w dziedzinie misteriów religijnych .
Największym centrum literackim był Glasney College , trzeci pod względem wieku uniwersytet brytyjski .
