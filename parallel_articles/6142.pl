Kornwalia
Kornwalia ( [ ] / [ ] ; korn .
`` Kernow '' [ ] / [ ] ) – kraina geograficzno-historyczna i zarazem najbardziej wysunięte na południowy zachód hrabstwo Anglii , położone na Półwyspie Kornwalijskim , na zachód od rzeki Tamar .
Centrum administracyjnym , a także jedynym miastem o statusie `` city '' jest Truro .
Kornwalia zajmuje powierzchnię 3563 km² łącznie z wyspami Scilly ( administracyjnie stanowiącymi oddzielną jednostkę ) , położonymi 45 km od Land ’ s End – najbardziej na południowy zachód wysuniętego fragmentu hrabstwa .
Średnia gęstość zaludnienia hrabstwa wynosi 144 os./km² .
Kornwalię charakteryzują dzikie krajobrazy wrzosowiska , długa i urozmaicona linia brzegowa oraz łagodny klimat .
Krajobraz górniczy Kornwalii i Zachodniego Devonu w roku 2006 wpisano na Listę Światowego Dziedzictwa Kulturalnego i Przyrodniczego UNESCO .
Istotną dziedziną gospodarki regionu jest turystyka ( 24 % przychodu ) .
Kornwalia jest jednak jednym z najbiedniejszych regionów Wielkiej Brytanii ( średnia płaca wynosi 62 % w stosunku do średniej wysokości wynagrodzeń w Wielkiej Brytanii ) z najmniejszym wkładem do gospodarki narodowej .
Kornwalijczycy to jeden z sześciu narodów celtyckich .
Niektórzy mieszkańcy protestują przeciw obecnemu statusowi politycznemu Kornwalii i domagają się większej autonomii .
Podział administracyjny hrabstwa .
Obecny .
W skład hrabstwa ceremonialnego wchodzą dwie jednolite jednostki administracyjne ( `` unitary authority '' ) .
Do 2009 .
Do 2009 Kornwalia składała się z siedmiu dystryktów .
Podział na dystrykty odzwierciedla mapa .
Obecnie całe hrabstwo oprócz wysp Scilly stanowi unitary authority , a wyspy Scilly stanowią drugą taką jednostkę .
Nazewnictwo .
Nazwa Kornwalii ( korn .
`` Kernow '' ) pochodzi od zamieszkującego niegdyś obecny obszar półwyspu celtyckiego plemienia , które Rzymianie nazywali `` Cornovii '' .
Słowo to pochodziło od jeszcze wcześniejszej nazwy plemienia Brytów – `` Corneu '' .
Celtycki rdzeń `` cern '' lub łacińska nazwa `` cornu '' – oba oznaczające „ róg ” lub „ półwysep ” – mogą wskazywać , że nazwa odnosi się do kształtu Kornwalii .
Podobnie używana od ok. 700 n.e .
nazwa `` Cornubia '' oznaczająca „ ludzi rogu ” nawiązuje do kształtu półwyspu .
W VI i VII wieku nazwa `` Cornubia '' przeszła liczne zmiany w związku z ewolucją języka staroangielskiego .
Anglosasi wprowadzili przyrostek `` -wealas '' ( „ cudzoziemcy ” ) , który w połączeniu z jedną z nowszych form utworzył określenie `` Corn-wealas '' .
