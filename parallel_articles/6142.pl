W 1497 roku wśród górników kornwalijskich wybuchła rebelia , będąca eskalacją protestu przeciw podniesieniu podatków przez Henryka VII , który zbierał fundusze na wojnę ze Szkocją .
Rebelianci dotarli do Londynu , zyskując po drodze zwolenników , jednak zostali pokonani w bitwie pod Deptford Bridge .
Kornwalijczycy również wzięli udział w tzw .
rebelii modlitewników ( `` Prayer book rebellion '' ) w 1549 będącą odpowiedzią na wprowadzenie modlitewników protestanckich , co było zresztą powszechnym zjawiskiem w południowo-zachodniej Anglii .
Nie chodziło tu tylko o tradycyjną katolickość regionu ; nowy modlitewnik napisany był tylko w języku angielskim , który był wówczas bardziej rozpowszechniony w regionie .
