Język koreański
Język koreański ( kor .
, ) – język narodowy Koreańczyków , urzędowy w Korei Południowej i Północnej , a także w chińskiej prefekturze autonomicznej Yanbian .
Łącznie na świecie językiem tym posługuje się 77,2 mln ludzi , wliczając skupiska użytkowników w Chinach , Stanach Zjednoczonych , Japonii , Rosji i Kazachstanie .
Oryginalny koreański system zapisu – hangul – jest alfabetem sylabicznym i fonetycznym .
Coraz rzadziej spotyka się zapis znakami sinokoreańskimi hancha .
