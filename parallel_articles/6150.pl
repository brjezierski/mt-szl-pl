Wola Korytnicka
Wola Korytnicka – wieś w Polsce położona w województwie mazowieckim , w powiecie węgrowskim , w gminie Korytnica .
Wieś królewska Wola Korycka położona była w drugiej połowie XVI wieku w powiecie liwskim ziemi liwskiej województwa mazowieckiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
