Koryciski
Koryciski ( zwane też : `` Koryciska '' , `` Korytyski '' , `` Korycisk '' ) – wieś w Polsce położona w województwie podlaskim , w powiecie hajnowskim , w gminie Dubicze Cerkiewne .
W latach 1975–1998 miejscowość administracyjnie należała do województwa białostockiego .
Wieś w 2011 roku zamieszkiwało 101 osób .
Prawosławni mieszkańcy wsi należą do parafia św .
Michała Archanioła w Starym Korninie , a wierni kościoła rzymskokatolickiego do parafii św .
Zygmunta w Kleszczelach .
Natomiast zielonoświątkowcy uczęszczają do Zboru Zielonoświątkowego w Dubiczach Cerkiewnych .
