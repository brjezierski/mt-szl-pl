Korzękwice
Korzękwice ( niem .
`` Korkwitz '' , 1936–1945 `` Moeckendorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Pakosławice .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
Pierwotnie miejscowość nosiła nazwę `` Korzekwica '' lub `` Korzekwice '' .
W dokumentach nazwa w zlatynizowanej formie `` Corequicz '' pojawiła się po raz pierwszy w 1273 roku .
Do języka niemieckiego została ona zaadaptowana w formie `` Korkwitz '' .
Etymologia związana jest z apelatywem `` korzekwica '' , dawną nazwą żabieńca babki wodnej , lub z nazwą osobową `` Korzekwa '' , `` Korzkwa '' , wywodzącą się od rzeczownika `` korzkiew '' w znaczeniu ‘ czerpak do nabierania wody ’ .
W 1936 roku administracja niemiecka zmieniła nazwę `` Korkwitz '' na `` Moeckendorf '' , która nawiązywała do nazwiska właściciela wsi z XIX wieku – Moeckego .
W 1948 roku zatwierdzono polską nazwę w formie `` Korzekwice '' , chociaż lokalna ludność powszechnie używała formy `` Korzękwice '' .
Forma `` Korzękwice '' jako nazwa urzędowa obowiązuje od 1 stycznia 2019 roku .
