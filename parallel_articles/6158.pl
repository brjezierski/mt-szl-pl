Koskowo
Koskowo – wieś w Polsce położona w województwie mazowieckim , w powiecie ostrowskim , w gminie Stary Lubotyń .
Wierni kościoła rzymskokatolickiego należą do parafii Nawiedzenia Najświętszej Maryi Panny w Starym Lubotyniu .
Historia .
W latach 1921 – 1939 wieś leżała w województwie białostockim , w powiecie łomżyńskim , w gminie Lubotyń .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 335 osób w 50 budynkach mieszkalnych .
