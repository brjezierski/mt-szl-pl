Kosmonosy
Kosmonosy − miasto w Czechach , w kraju środkowoczeskim .
Według danych z 31 grudnia 2003 powierzchnia miasta wynosiła 1 140 ha , a liczba jego mieszkańców 4 904 osób .
Prawa miejskie 25 czerwca 1913 .
W latach 1947–1954 i 1974–1989 miejscowość była przyłączona do Mladá Boleslav , od 1990 ponownie samodzielne miasto .
