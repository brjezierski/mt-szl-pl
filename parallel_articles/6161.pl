Wola Kosnowa
Wola Kosnowa – wieś w Polsce położona w województwie małopolskim , w powiecie nowosądeckim , w gminie Łącko .
Miejscowość położona jest w Beskidzie Wyspowym .
Pola i zabudowania Woli Kosnowej zajmują dolinę potoku Czarna Woda , południowe podnóża Modyni oraz północne i zachodnie zbocza Skalicy .
Integralnymi częściami wsi są : Bukówki , Cerchla , Dąbrowy , Długa Łąka , Dudówka , Gębacze , Gromale , Janeczki , Jaszkowiec , Kierczówka , Kuklina , Kwartały , Markowa , Moraźnica , Pyrdoły , Tomaszki , Wierch-Góry , Zagóra , Zwierzenica .
Wieś była własnością klasztoru klarysek w Starym Sączu , położoną w drugiej połowie XVI wieku w powiecie sądeckim województwa krakowskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa nowosądeckiego .
Ochotnicza Straż Pożarna
Ochotnicza Straż Pożarna w Woli Kosnowej działa od 1958 roku i posiada samochód bojowy MAN 14.224 .
