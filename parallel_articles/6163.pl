Kosowo jest serbskie
Kosowo jest serbskie ( , Kosovo je Srbija ; dosłownie `` Kosowo to Serbia '' ) – slogan używany w Serbii od 2004 roku , spopularyzowany zwłaszcza po jednostronnym ogłoszeniu przez Kosowo deklaracji niepodległości 17 lutego 2008 .
Slogan został użyty podczas serii protestów , także przez serbski rząd .
Pojawia się na odzieży oraz graffiti , w 2009 roku w wyniku ataku hakerów został umieszczony na witrynach należących do Kosowa .
Używany na całym świecie , głównie przez Serbów i Rosjan .
W 2008 roku serbski pływak Milorad Čavić został wykluczony z mistrzostw Europy po tym , jak po zdobyciu złotego medalu na 50 m stylem motylkowym na ceremonii medalowej zaprezentował koszulkę z napisem „ Kosovo je Srbija ” .
