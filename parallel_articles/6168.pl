Kosrae ( grupa etniczna )
Kosrae ( także : `` Kusai '' ) – grupa etniczna w Oceanii , rdzenni mieszkańcy wyspu Kosrae w Sfederowanych Stanach Mikronezji , odłam Mikronezyjczyków .
W 1996 r. ich liczebność wynosiła ok. 8 tys .
Kosrae posługują się językiem kusajskim ( kosrae ) z grupy mikronezyjskiej oraz językiem angielskim .
Wyznają głównie protestantyzm .
