Kostaryka
Kostaryka ( `` Costa Rica '' , hiszp .
dosł .
‘ bogate wybrzeże ’ , Republika Kostaryki – `` República de Costa Rica '' ) – państwo w Ameryce Centralnej nad Morzem Karaibskim i Oceanem Spokojnym .
Graniczy od północy z Nikaraguą na odcinku 309 km i od południa z Panamą – 330 km .
Łączna długość wybrzeża karaibskiego i pacyficznego wynosi 1290 km .
Geografia .
Kostaryka to kraj wyżynno-górzysty , położony w strefie aktywności sejsmicznej .
Znajdują się tu także liczne wulkany , niektóre aktywne .
Niziny występują jedynie na wybrzeżach oraz w dolinach większych rzek : San Juan , Tempisque i Cañas .
Przez środek kraju biegną trzy główne pasma górskie : Cordillera de Guanacaste ( wulkan Miravalles , 2021 m n.p.m .
) , Cordillera Central ( wulkany Irazú , 3432 m n.p.m .
i Turrialba , 3421 m n.p.m . )
oraz Cordillera de Talamanca z najwyższym szczytem Kostaryki Chirripó Grande ( 3820 m n.p.m . ) .
Między pasmami Cordillerą Central a C. de Talamanca znajduje się niewielka wyżyna Cartago , położona na wysokości 1400 m n.p.m .
U podnóży Kordillery Centralnej rozciąga się zajmująca ok. 900 km² śródgórska kotlina Meseta Centralna .
Tutaj ulokowała się stolica kraju San José i inne ważne miasta Kostaryki ( Cartago , Alajuela , Heredia ) .
W sumie mieszka tu ponad połowa mieszkańców kraju .
