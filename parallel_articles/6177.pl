Koszalin
Koszalin ( , , ) – miasto na prawach powiatu w północno-zachodniej Polsce , w województwie zachodniopomorskim .
Położone na Pobrzeżu Koszalińskim , nad rzeką Dzierżęcinką , nad jeziorami : Jamno i Lubiatowo Północne .
Siedziba powiatu koszalińskiego , sądu okręgowego , wydziału zamiejscowego urzędu marszałkowskiego i delegatury urzędu wojewódzkiego .
Według danych GUS z 30 czerwca 2021 r. , Koszalin liczył mieszkańców i był pod względem liczby ludności drugim ( po Szczecinie ) miastem w województwie zachodniopomorskim , a także 37. spośród najludniejszych miast w Polsce .
Miasto jest znaczącym ośrodkiem kulturalnym – działają tu teatry , kina , filharmonia , amfiteatr oraz muzea .
Jest także siedzibą lokalnych mediów .
W Koszalinie znajduje się również kuria diecezji koszalińsko-kołobrzeskiej .
Koszalin jest największym miastem regionu środkowopomorskiego .
Centrum miasta znajduje się ok. 11 km od Morza Bałtyckiego .
Obszar miasta sięga południowego brzegu nadmorskiego jeziora Jamno .
Położenie .
Miasto położone jest na Pobrzeżu Koszalińskim , a dokładnie znajduje się na styku granic trzech mezoregionów : Wybrzeża Słowińskiego , Równiny Białogardzkiej i Równiny Słupskiej .
Po zmianach granic w 2010 roku miasto leży nad jeziorem Jamno .
Powierzchnia miasta wynosi 98,34 km² ( 1 stycznia 2014 ) .
Historycznie Koszalin jest położony na Pomorzu Zachodnim .
W latach 1815–1945 miasto było siedzibą rejencji koszalińskiej .
W latach 1946–1950 znajdowało się w województwie szczecińskim .
