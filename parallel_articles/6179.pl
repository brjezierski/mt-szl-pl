Wola Koszucka
Wola Koszucka – wieś w Polsce położona w województwie wielkopolskim , w powiecie słupeckim , w gminie Lądek .
Wieś duchowna `` Wola '' , własność opata cystersów w Lądzie pod koniec XVI wieku leżała w powiecie konińskim województwa kaliskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa konińskiego .
