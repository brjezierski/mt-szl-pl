Kotarszyn
Kotarszyn – wieś w Polsce , położona w województwie świętokrzyskim , w powiecie ostrowieckim , w gminie Waśniów .
Był wsią klasztoru norbertanek buskich w województwie sandomierskim w ostatniej ćwierci XVI wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
22 sierpnia 1347 r. król Kazimierz Wielki zezwolił klasztorowi norbertanek w Busku na przeniesienie wsi Kotarszyn , Nosów i Piotrów z prawa polskiego na prawo średzkie .
Na początku stycznia 2009 r. przy budowie lokalnej sieci wodociągowej , podczas prowadzonych tu prac ziemnych , asystujący robotom archeolodzy natrafili na pozostałości budowli mieszkalnej i ceramikę z epoki neolitycznej .
Odkryty fragment chaty z glinianą polepą oraz skorupy naczyń należą do kultury amfor kulistych .
Archeolodzy z Muzeum Historyczno-Archeologicznego w Ostrowcu Świętokrzyskim ocenili wstępnie wiek znaleziska na około 4,5 tys .
