Koulikoro
Koulikoro ( bambara : `` Kulikɔrɔ '' ) – miasto w Mali nad rzeką Niger , ośrodek administracyjny regionu Koulikoro , ok. 26 000 mieszkańców .
Miasto jest końcową stacją linii kolejowej Dakar-Niger .
W okresie od sierpnia do listopada możliwy jest transport rzeką Niger do Ségou , Mopti , Timbuktu i Gao .
