Kowal ( rodzaj )
Kowal ( `` Pyrrhocoris '' ) – rodzaj pluskwiaków z podrzędu różnoskrzydłych i rodziny kowalowatych .
Pluskwiaki średnich lub dużych rozmiarów , o krępym , owalnym w zarysie ciele .
Wierzch ciała mają spłaszczony , zaś spód wypukły .
Głowa ich zaopatrzona jest w dłuższy od policzków nadustek , stosunkowo długie , czteroczłonowe czułki , również czteroczłonową kłujkę i pozbawiona jest przyoczek .
Kształt przedniej części głowy jest trójkątny .
