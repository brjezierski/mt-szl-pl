Kownaciska
Kownaciska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Suchożebry .
Wieś posiadała w 1673 roku podkomorzyna koronna Konstancja Butlerowa , leżała w ziemi drohickiej województwa podlaskiego w 1795 roku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Mikołaja w Krześlinie .
W miejscowości działa założona w 1928 roku jednostka ochotniczej straży pożarnej .
