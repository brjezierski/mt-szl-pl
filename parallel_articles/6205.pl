Kozielno
Kozielno – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Paczków .
Kozielno położone jest na Obniżeniu Otmuchowskim w pobliżu głównej rzeki krainy Nysy Kłodzkiej .
W południowo-zachodniej części województwa opolskiego , od północy graniczy z Jeziorem Paczkowskim , od strony południowej ze wsią Kamienica , od zachodu z Błotnicą , która leży w województwie dolnośląskim , a od wschodu z miastem Paczków .
Historia .
Przyjmuje się , iż osada Cozelno powstała w 1254 roku .
Powstaniu jej sprzyjały korzystne warunki naturalne .
Stary ośrodek osadnictwa ukształtowany na prawobrzeżnych terenach Nysy Kłodzkiej był obszarem stale i silnie kolonizowanym do końca średniowiecza , gdyż tutaj uderzył pierwszy impet kolonizacji niemieckiej od XIII w .
Kozielno uniknęło zniszczeń wojennych podczas II wojny światowej .
Do Kozielna po wojnie w ramach zasiedlania Ziem Zachodnich przyjechało około 120 osób , w tym najliczniejszą grupę stanowili osadnicy z dawnego województwa Krakowskiego .
Pozostali to osadnicy z byłych województw : katowickiego , łódzkiego , rzeszowskiego , kieleckiego .
