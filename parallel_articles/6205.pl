Współczesność .
Spis powszechny ludności w 2011 wykazał , że w Kozielnie mieszka 317 mieszkańców .
Teren wsi leży w Otmuchowsko-Nyskiego Obszaru Chronionego Krajobrazu .
W Kozielnie znajduje się studnia głębinowa , zaopatrująca w wodę całż wieś .
