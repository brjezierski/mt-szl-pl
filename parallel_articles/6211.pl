Kołaczyce ( gmina )
Kołaczyce – gmina miejsko-wiejska w województwie podkarpackim , w powiecie jasielskim .
W latach 1975–1998 gmina położona była w województwie krośnieńskim .
Leży na prawym brzegu Wisłoki u podnóża góry Liwocz , na terenie Pogórza Strzyżowskiego i Doliny Wisłoki .
