Kołomyja
Kołomyja ( ukr .
Коломия ) – miasto na zachodniej Ukrainie , w obwodzie iwanofrankiwskim , nad Prutem , siedziba rejonu kołomyjskiego .
W mieście rozwinął się przemysł maszynowy , odzieżowy , obuwniczy , metalowy , spożywczy , drzewny oraz papierniczy .
Miasto jest centrum huculskiej sztuki ludowej , znajduje się tu m.in .
muzeum etnograficzne i muzeum pisanek .
Miasto królewskie lokowane pomiędzy 1366 i 1370 rokiem położone było w XVI wieku w województwie ruskim .
Kołomyja uzyskała prawo składu w 1456 roku .
Historia .
XIII – XVIII wiek .
Nazwa Kołomyi ma związek z założycielem miasta królem halickim Kolomanem .
Miejscowość była wzmiankowana w 1240 roku i należała do księstwa halicko-wołyńskiego .
