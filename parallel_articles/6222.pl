Obecna świątynia została zbudowana w latach 1730–1738 w stylu barokowym , według projektu prudnickiego architekta Jana Innocentego Töppera .
Pierwowzorem dla niej był kościół św .
Mikołaja w Pradze w dzielnicy Malá Strana , na Rynku Małostrańskim .
Obrazy na chórze kościoła zostały namalowane w 1735 przez Feliksa Antona Schefflera .
2 lipca 1752 nastąpiło uroczyste poświęcenie kościoła przez biskupa wrocławskiego Philippa Gottharda von Schaffgotscha .
28 lutego 1779 miasto zostało zaatakowane przez austriackie wojska i kościół został ponownie uszkodzony .
Dopiero w 1860 roku w ramach przeprowadzonego remontu obiekt zyskał nowy dach – z blachy oraz nową posadzkę .
W 1803 nadbudowano wieżę .
W 1898 w powiększonych wcześniej otworach okiennych prezbiterium umieszczono witraże ze scenami ze Starego Testamentu .
Kościół w 1917 utracił dzwony na potrzeby wojenne , lecz zyskał nowe w 1929 .
Podczas bitwy o Prudnik 23 kwietnia 1945 na północny przedsionek kościoła spadła bomba lotnicza , przez co zniszczeniu uległo boczne wejście .
W 1946 parafię przejęli dominikanie , którzy założyli tu swój klasztor i którzy przebywali w Prudniku aż do 1999 , kiedy to przekazali wiernych pod opiekę księży diecezjalnych .
W lutym 1947 w kościele doszło do napadu .
Lokalna gazeta Nasz Głos opisała zdarzenie następująco : „ Kościół farny w Prudniku stał się miejscem zuchwałego napadu bandyckiego , połączonego ze świętokradztwem .
Wieczorem 16 lutego 1947 roku o godzinie 19 czterech zamaskowanych i uzbrojonych bandytów wtargnęło do zakrystii w czasie , kiedy kapłan udzielał ślubu nowożeńcom .
Bandyci sterroryzowali bronią obecnych i podnieśli świętokradzką rękę na ołtarz .
Z tabernakulum wyjęli monstrancję srebrną , pozłacaną , którą zrabowali , poczem wycofali się , grożąc bronią .
Oddział śledczy Milicji Powiatowej prowadzi energiczne dochodzenie i podobno jeden ze sprawców napadu został poznany ” .
