Michała Archanioła w Prudniku
Kościół św .
Michała Archanioła w Prudniku – zabytkowy , późnobarokowy kościół katolicki w Prudniku , znajdujący się w południowej części Starego Miasta , przy Placu Farnym .
Świątynia wpisuje się w szachownicowy układ miasta założonego w XIII wieku na prawie niemieckim .
Historia .
Pierwszy drewniany kościół , pierwotnie pod wezwaniem Wniebowzięcia Najświętszej Maryi Panny , na terenie Prudnika został wzniesiony w drugiej połowie XIII wieku , podczas postępowania procesu kolonizacyjnego prowadzonego przez marszałka Woka z Rożemberka .
Według proboszcza Macieja Alojzego Scharkowa , parafia powstała w 1279 , kiedy to syn Woka , Henryk z Rożemberka , lokował Prudnik na prawie niemieckim .
Pierwsze pisemne wzmianki o kościele w Prudniku pochodzą z 1321 i 1331 .
Dokumenty wspominają pierwszego prudnickiego proboszcza o imieniu Milotha .
W wyniku reformacji od 1554 kościół należał do luteranów .
Po odzyskaniu go przez katolików w 1612 rozpoczęto budowę nowego kościoła w stylu renesansowym z inicjatywy Macieja Bilicera , jednakże w 1627 , jeszcze przed ukończeniem budowy , miasto znów padło ofiarą pożaru .
Spłonęły : wieża , organy , dzwony i dach .
Dopiero w 1638 doprowadzono świątynię do używalności .
Po wizytacji w 1666 dokonano poświęcenia kościoła pod wezwaniem Wniebowzięcia Najświętszej Maryi Panny i św .
Michała Archanioła .
W 1722 kościół posiadał już sześć ołtarzy bocznych : Zbawiciela , św .
