Kościół św .
Michała Archanioła zbudowany został w stylu późnobarokowym .
Jest to kościół halowy murowany z cegły , trójnawowy , z barokowym ołtarzem głównym .
W skromnym z zewnątrz korpusie kryje olbrzymie sklepienie kolebkowe z umieszczonym w tęczy herbem Prudnika .
Wewnątrz olbrzymia galeria połączoną z chórem i wieżą , a także ołtarz z dwoma cherubinami oraz rzeźbą Chrystusa na zwieńczeniu .
Na tylnej ścianie prezbiterium umieszczono barokowy obraz przedstawiający Wniebowzięcie Matki Bożej .
W zakrystii znajduje się obraz Matki Boskiej Prudnickiej pochodzący ze zburzonego w roku 1907 klasztoru kapucynów oraz obraz Trójcy Świętej autorstwa Feliksa Antona Schefflera .
Kościół posiada 9 ołtarzy bocznych .
Na ołtarzu głównym znajduje się naturalnej wielkości figura Najświętszego Serca Jezusa autorstwa rzeźbiarza Thamma z Nysy .
