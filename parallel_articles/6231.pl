Kościerzyna ( stacja kolejowa )
Kościerzyna – stacja kolejowa położona we wschodniej części Kościerzyny , przy ul .
Dworcowej .
Powstała w 1885 , natomiast budynek pochodzi z okresu międzywojennego .
Znajdują się tu dwa perony .
Stacja stanowi lokalny węzeł kolejowy , na którym krzyżują się linie kolejowe 201 , 211 oraz rozebrana już 233 .
Obok , na terenie dawnej lokomotywowni , znajduje się Muzeum Kolejnictwa w Kościerzynie .
W roku 2018 stacja obsługiwała ok. 2400 pasażerów na dobę .
Położenie .
Stacja Kościerzyna znajduje się przy ulicy Dworcowej we wschodniej części miasta .
Z centrum miasta można dojechać komunikacją miejską , której autobusy zatrzymują się na przystanku `` ul .
Dworcowa ( PKP ) '' .
Historia .
1885–1899 .
Pierwsza linia kolejowa dotarła do Kościerzyny w 1885 .
Była to rozebrana już linia kolejowa nr 233 Pszczółki–Kościerzyna , stanowiąca część sieci kolei wschodnich .
Linia Pszczółki–Kościerzyna umożliwiała dotarcie do linii Gdańsk–Tczew .
Najprawdopodobniej w tym samym roku powstała parowozownia .
