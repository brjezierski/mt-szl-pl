Koszyce ( Piła )
Koszyce ( ) – dzielnica Piły , położona w północnej części miasta , nad Gwdą .
Historia .
Pierwsza wzmianka o młynie `` na zdroju Kossicznim '' pojawia się w 1563 .
W 1789 oprócz młyna istniał tu królewski folwark wolny od podatków i 5 gospodarstw .
W latach 1807–1815 na strumieniu między Piłą i Koszycami przebiegała granica pomiędzy Księstwem Warszawskim a Królestwem Prus .
Wieś rozwinęła się w XIX wieku – w 1820 miała 49 , a w 1905 już 246 mieszkańców .
Aż do 1955 Koszyce były wsią leżącą w powiecie wałeckim , w tymże roku wraz z przysiółkiem Kuźnica Pilska zostały włączone w granice Piły .
Od lat 90 .
XX wieku na terenie dzielnicy ( zwłaszcza w jej wschodniej części ) intensywnie rozwija się budownictwo mieszkaniowe ( osiedle Zielona Dolina ) .
Dojazd .
