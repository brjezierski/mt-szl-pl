Koziegłowy ( powiat koniński )
Koziegłowy – wieś w Polsce położona w województwie wielkopolskim , w powiecie konińskim , w gminie Kleczew .
Miejscowość wchodzi w skład sołectwa Janowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa konińskiego .
W pobliżu wsi znajduje się grodzisko z okresu halsztackiego .
