Kozia Góra ( rejon postawski )
Kozia Góra – dawna kolonia .
Tereny na których leżała znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie postawskim , w sielsowiecie Kozłowszczyzna .
Historia .
W czasach zaborów w granicach Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym folwark a następnie kolonia leżała w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Łuck ( od 1927 gmina Kozłowszczyzna ) .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 9 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 13 domach zamieszkiwało 56 osób .
Miejscowość należała do parafii rzymskokatolickiej w Mosarzu i prawosławnej w Osinogródku .
Podlegała pod Sąd Grodzki w Duniłowiczach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Kozłowszczyźnie .
