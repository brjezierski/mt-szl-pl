Kołnierz
Kołnierz – wykończenie ubioru przy szyi , wykonane z tej samej tkaniny i na stałe połączone z ubiorem lub z innego materiału , przyszywane , przypinane lub nakładane .
Stosowane od XIV wieku .
Krój kołnierzy zmieniał się znacznie na przestrzeni wieków .
Początkowo pojawił się w stroju męskim w drugiej połowie XVI wieku , jako część szerokiej szaty spiętej w talii pasem , zwanej houppelande .
W tym okresie we Francji i w Anglii zaczęto dodawać prosty kołnierz do męskich kaftanów .
Początkowo stosunkowo niski , w pierwszych latach XV wieku sięgający już do uszu .
Dostojnicy i królowie nosili także płaszcze z futrzanymi kołnierzami .
W Hiszpanii w XVI wieku modne stały się wśród mężczyzn bogato zdobione kaftany z wysokimi kołnierzami , spod których wystawały marszczone kołnierzyki koszuli .
Kołnierze były także obecne w damskich sukniach wierzchnich .
W XVI wieku we Francji pojawiła się moda na kryzy zarówno u mężczyzn , jak i u kobiet .
