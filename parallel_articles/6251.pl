Kranj
Kranj ( niem .
`` Krainburg '' ) – czwarte pod względem wielkości miasto w Słowenii , położone 20 km na północny zachód od Lublany , w Górnej Krainie .
W 2018 roku liczyło 37 313 mieszkańców .
Miasto ma tradycje w przemyśle oponiarskim i elektronicznym ( Gorenjske elektrarne , Iskraemeco , Iskratel , Sava ) .
Ma zachowane stare centrum miasta powyżej ujścia rzek Kokra i Sawa .
Znane jest także jako miasto sportu , które ma bogatą bazę sportową .
Znajduje się tu boisko do piłki nożnej ze sztuczną i naturalną trawą , korty tenisowe , boiska do koszykówki oraz baseny : kryty i odkryty , a także sztuczna ściana wspinaczkowa na której wielokrotnie odbywały się zawody o randze ogólnoeuropejskiej .
