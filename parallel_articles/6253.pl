Krapkowice
( , ) – miasto w Polsce położone w województwie opolskim , w powiecie krapkowickim , siedziba gminy miejsko-wiejskiej Krapkowice .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej , w Kotlinie Raciborskiej , będącej częścią Niziny Śląskiej .
Przepływają przez nie rzeki Odra i Osobłoga .
W latach 1975–1998 miasto administracyjnie należało do województwa opolskiego .
Według danych na 31 grudnia 2020 miasto było zamieszkane przez 15 962 osoby .
Geografia .
Położenie .
Miasto jest położone w południowo-zachodniej Polsce , w województwie opolskim , około 24 km od granicy z Czechami , w Kotlinie Raciborskiej , przy granicy gminy Krapkowice z gminą Gogolin .
Krapkowice położone są na wysokości 170 m n.p.m. , u ujścia rzeki Osobłogi do Odry .
Znaczna część starej części Krapkowic i Otmętu leży na wapieniach triasu ( formacja gogolińska ) , które były wydobywane w kilku kamieniołomach zachowanych częściowo do dziś , choć już nieczynnych , podobnie jak zespół starych pieców wapienniczych .
Przez miasto przepływa Odra , stąd są tu także osady rzeczne kenozoiku , a sama rzeka tworzy tzw .
