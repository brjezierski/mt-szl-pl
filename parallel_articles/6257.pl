W mieście działają : muzeum antyfaszystów niemieckich oraz państwowe archiwum dokumentów fotograficznych i audiowizualnych .
Znajduje się tu także osiemnastowieczny zespół architektoniczny Znamienskoje-Gubajłowo , dawna siedziba księcia Wasilija Dołgorukowa-Krymskiego .
Historia .
W połowie XIX wieku na terenie dzisiejszego Krasnogorska utworzone zostało osiedle Bańki , które wzięło swoją nazwę od miejscowej rzeki .
W 1926 do Baniek przeniosły się zakłady optyczno-mechaniczne i dla ich robotników założono w pobliżu osiedle Krasnaja Gorka .
W 1932 połączono ze sobą wsie Pawszyno ( znana od 1462 ) i Gubajłowo ( znana od 1620 ) oraz osiedla Krasnaja Gorka i Bańki , tworząc nowe osiedle o nazwie Krasnogorsk .
W 1940 osiedle Krasnogorsk otrzymało prawa miejskie .
W latach 1945-1947 w tamtejszym obozie dla internowanych przetrzymywani byli członkowie rodzin Krasickich , Radziwiłłów ( Janusz Radziwiłł wraz z żoną Anną ) , Branickich ( m.in .
Adam Branicki , Anna Branicka ) oraz Zamoyskich .
Z uwagi na przemysł w mieście powstał zamysł ( niezrealizowany ) aby przemianować Krasnogorsk na Optikogorsk .
