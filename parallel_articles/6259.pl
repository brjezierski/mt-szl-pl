Krasnojarsk
Krasnojarsk ( ) – miasto w Rosji , trzecie co do wielkości miasto Syberii , położone nad rzeką Jenisej ; stolica Kraju Krasnojarskiego ; 1 093 771 mieszkańców ( 2020 ) .
Wraz z aglomeracją liczy 1,155 mln mieszkańców ( stan na 2012 ) , dzięki czemu stanowi 12. miasto w Rosji pod względem liczby ludności .
Port rzeczny , węzeł kolejowy .
W okolicy Park Narodowy „ Słupy Krasnojarskie ” .
Historia .
Miasto zostało założone w lipcu 1628 roku przez kozaków bojara Andrzeja Dubieńskiego jako ostróg dla obrony przed Tatarami , zamieszkującymi wybrzeże Jenisieju .
Pierwotną nazwą miasta był „ Krasnyj jar ” od tatarskiej nazwy miejscowości „ Kyzył Dżar ” , czyli „ czerwone wzgórze ” .
Obecną nazwę nadano po otrzymaniu praw miejskich w 1690 roku .
