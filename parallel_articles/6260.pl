Krasnoperekopsk
Krasnoperekopsk ( ukr .
`` Красноперекопськ '' , ros .
`` Красноперекопск '' , tatarski `` Krasnoperekopsk '' ) – miasto o znaczeniu regionalnym na Krymie , obszar uznawany przez większość państw jako część Ukrainy , włączony `` de facto '' do Federacji Rosyjskiej w marcu 2014 roku jako część Republiki Krymu .
Miejscowość jest położona na Przesmyku Perekopskim ( Krym ) , stolica regionu krasnoperekopskiego .
Założone w 1932 , jako osiedle pracowników fabryki bromu .
Prawa miejskie uzyskało w 1966 .
Liczy około 33 000 mieszkańców , z czego ponad połowa to Rosjanie , nieco mniej jest Ukraińców ; mieszka tu również około 1100 Tatarów krymskich .
W mieście znajduje się największa fabryka sody na Ukrainie .
Przez miasto przebiega linia łącząca Krym z miastem Chersoń .
