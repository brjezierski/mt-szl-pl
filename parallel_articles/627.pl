2016 ( album )
2016 – album Lutosławski Quartet z utworami współczesnych , polskich kompozytorów : Marcina Markowicza , Andrzeja Kwiecińskiego i Pawła Mykietyna , wydany w 2017 przez Narodowe Forum Muzyki i CD Accord ( nr kat .
odpowiednio : NFM 37 , ACD 233-2 ) .
Album uzyskał nominację do Fryderyka 2018 w kategorii Album Roku - Muzyka Współczesna .
