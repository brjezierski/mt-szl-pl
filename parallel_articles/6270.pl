Krefeld
Krefeld ( do 1929 `` Crefeld '' ) – miasto na prawach powiatu w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf , położone na południowy zachód od Zagłębia Ruhry w pobliżu Renu .
Historia .
Już za czasów rzymskich istniał obóz `` Gelduba '' , a Krefeld pierwszy raz zostało wzmiankowane w 1105 r. W XVII wieku wojna trzydziestoletnia ominęła Krefeld .
W 1683 r. trzynaście rodzin opuściło Krefeld i założyło Germantown koło Filadelfii w stanie Pensylwania .
Dzielnica Linn była do 1901 odrębnym miastem , Uerdingen do 1929 r .
