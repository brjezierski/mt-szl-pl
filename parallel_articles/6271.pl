Saara ( rodzaj )
Saara – rodzaj jaszczurki z podrodziny Uromastycinae w rodzinie agamowatych ( Agamidae ) .
Zasięg występowania .
Rodzaj obejmuje gatunki występujące w Iranie , Iraku , Afganistanie , Indiach i Pakistanie .
