Kreminna
Kreminna ( ) – miasto na wschodzie Ukrainy w obwodzie ługańskim .
Ośrodek przemysłu spożywczego i wydobywczego .
Miejscowość jest znana jako „ płuca Donbasu ” , ponieważ ma dobre możliwości rekreacyjne ( lasy , jeziora , rzeki , obozy rekreacyjne dla dzieci ) .
Miasto posiada nowoczesny ośrodek sportowy do treningu sportowców olimpijskich i paraolimpijskich .
Historia .
Za rok założenia Kreminnej uważa się 1679 .
Zbiegli chłopi pańszczyźniani z prawobrzeżnej Ukrainy założyli osadę nad brzegiem rzeki Krasnej .
W pierwszej ćwierci XIX wieku wieś uzyskała status osady wojskowej i do początku XX wieku nosiła nazwę Nowogłuchow na cześć stacjonującego tu pułku Głuchowskiego .
