Kremnica
Kremnica ( , , ) – miasto w środkowej Słowacji , w kraju bańskobystrzyckim .
Leży w Górach Kremnickich .
Ośrodek turystyki , hutnictwa oraz siedziba mennicy państwowej .
Historia .
Podstawą powstania i rozwoju miasta było złoto , którego wydobycie zaczęto prowadzić w okolicy być może już w X , a najpóźniej w połowie XI w. Obok złota wydobywano tu również srebro , a z czasem i rudy innych metali .
Początków osady , będącej zaczątkiem dzisiejszej Kremnicy , należy szukać prawdopodobnie w połowie XIII w. 17 listopada 1328 r. osada `` Cremnychbanya '' otrzymała od króla węgierskiego Karola Roberta prawa : górnicze i mennicze na wzór czeskiej Kutnej Hory oraz przywileje wolnego królewskiego miasta górniczego i menniczego .
Błyskawiczny rozwój miasto zawdzięczało zwłaszcza mennicy królewskiej , w której od 1329 r. bito srebrne węgierskie grosze , a od 1335 r. złote floreny , znane jako kremnickie dukaty oraz małe , srebrne denary .
