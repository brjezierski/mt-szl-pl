Pączek ( model ekonomiczny )
Model ekonomiczny obwarzanka – wizualny model dla zrównoważonego rozwoju – w kształcie obwarzanka – łączy pojęcie granic planetarnych z koncepcją komplementarnych granic społecznych .
Zaproponowany model uwzględnia wyniki gospodarki pod względem stopnia zaspokojenia potrzeb ludzi bez przekraczania pułapu ekologicznego Ziemi .
Nazwa pochodzi od kształtu diagramu , tzn .
dysku z otworem pośrodku .
Centralna dziura modelu przedstawia odsetek osób , które nie mają dostępu do podstawowych życiowych zasobów ( opieki zdrowotnej , edukacji , równości i tak dalej ) , podczas gdy powłoka reprezentuje pułapy ekologiczne ( granice planetarne ) , od których zależy życie i których nie wolno przekraczać .
W związku z tym gospodarkę uważa się za dobrze prosperującą , gdy wszystkie dwanaście fundamentów społecznych zostanie osiągniętych bez przekroczenia któregokolwiek z dziewięciu pułapów ekologicznych .
Sytuację tę reprezentuje obszar między dwoma pierścieniami , mianowicie bezpieczna i sprawiedliwa przestrzeń dla ludzkości .
Schemat został opracowany przez ekonomistkę z Oksfordu Kate Raworth w artykule Oxfam `` Bezpieczna i sprawiedliwa przestrzeń dla ludzkości '' i rozwinięty w książce `` Donut Economics : Seven Ways to Think Like a 21st-Century Economist ( Ekonomia obwarzanka .
