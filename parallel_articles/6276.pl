Kreta
Kreta ( , od słowa `` krateia '' – `` silna '' , , , wen .
`` Candia '' ) – grecka wyspa na Morzu Śródziemnym .
Największa wyspa Grecji i piąta pod względem powierzchni wyspa śródziemnomorska .
Linia brzegowa ma długość ok. 1040 km .
Największym miastem oraz siedzibą władz jest Heraklion ( Iraklion ) .
Na Krecie odkryto jedne z najstarszych , mające 5,7 mln lat ślady przodków człowieka .
Krajobraz wyspy tworzą skaliste wybrzeża , przeplatające się z szerokimi , jasnymi plażami .
Klimat ma cechy umiarkowane , jak i subtropikalne .
Średnia roczna opadów wynosi 640 mm .
Lato jest gorące i suche , w zimie jest chłodniej .
W rejonach górskich opady są dużo wyższe niż w reszcie wyspy , szczyty często pokrywa śnieg ( od listopada do maja ) .
Roślinność jest skąpa , bujniej rozwija się w wilgotniejszych rejonach .
Kreta stanowi jeden z regionów administracyjnych Grecji , w ramach którego można wyróżnić 4 jednostki regionalne : Chania , Retimno , Heraklion i Lasiti .
Transport samolotowy zapewniają dwa duże lotniska , port lotniczy Heraklion im .
Nikosa Kazandakisa w Heraklionie i Daskalogiannis w Chanii .
Największe miasta to : Heraklion ( Iraklion ) , Chania , Retimno , Jerapetra , Ajos Nikolaos oraz Sitia .
Warunki geologiczne .
Skały .
Kreta zbudowana jest ze skał osadowych - głównie wapieni .
Z tego powodu znajduje się tu wiele jaskiń , jak również wąwozy .
Historia przemian geologicznych .
Kreta powstała z wypiętrzających się podwodnych łańcuchów górskich .
15 milionów lat temu Kreta była częścią Europy kontynentalnej .
Zachodnie fragmenty wyspy są przedłużeniem Gór Dynarskich .
Kreta jest w ciągłym ruchu , obecnie przesuwa się na południe z prędkością ok. centymetra na ćwierć wieku i o tyle Afryka oddala się od Europy .
Wyspa jest umiejscowiona na tzw .
