Kreuth ( Bawaria )
Kreuth – miejscowość i gmina uzdrowiskowa w południowych Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , w powiecie Miesbach .
Leży w Alpach Bawarskich , około 17 km na południowy zachód od Miesbach , nad rzeką Weißach , przy granicy z Austrią i drodze B307 .
