Kristiansand ( stacja kolejowa )
Kristiansand – stacja kolejowa w Kristiansand , w okręgu Agder , w Norwegii .
Jest najbardziej na południe położoną stacją w kraju .
Położenie .
Leży w odległości 365 km od Oslo , 5,5 m n.p.m .
Znajduje się na linii Sørlandsbanen .
Ruch dalekobieżny .
Leży na linii Sørlandsbanen .
Jest stacją obsługującą dalekobieżne połączenia z południowo-zachodnią i południową częścią kraju .
Stacja przyjmuje sześć par połączeń dziennie do Arendal i Stavanger .
Obsługa pasażerów .
Kasy biletowe , automaty biletowe , kiosk , kawiarnia , telefon publiczny , parking krótkoterminowy , parking rowerowy , wypożyczalnia rowerów , przystanek autobusowy , postój taksówek .
