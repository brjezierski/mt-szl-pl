Krogulna
Krogulna ( śl .
: `` Krogulno '' , niem .
`` Krogullno-Gründorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie namysłowskim , w gminie Pokój , nad rzeką Stobrawą .
Krogulna leży 3 kilometry na północny zachód od Pokoju , 18 kilometrów na południowy wschód od Namysłowa i 31 kilometrów na północ od stolicy województwa – Opola .
W pobliżu wsi przebiega droga wojewódzka nr 454 .
Populacja wsi wynosi 283 osoby .
Nazwa .
Etymologia nazwy wsi wiąże się z przymiotnikiem pochodzącym od nazwy ptaka – krogulca .
W okresie hitlerowskiego reżimu w latach 1936-1945 miejscowość nosiła nazwę `` Stobertal '' .
Historia .
Najstarsza wzmianka o wsi pochodzi z roku 1309 .
