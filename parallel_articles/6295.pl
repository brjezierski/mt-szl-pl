Krosno ( narzędzie tortur )
Krosno – rodzaj narzędzia tortur przypominający łoże sprawiedliwości i często z nim mylony .
Była to stosowana już od czasów starożytnych drewniana lub żelazna prostokątna rama , do której przeciwległych boków przywiązywano ręce oraz nogi torturowanego .
Mogła mieć konstrukcję poziomą lub pionową .
W poprzek ramy krosna czasami mocowano również listwy , przez co konstrukcja przypominała drabinę .
