Krościenko nad Dunajcem ( gmina )
Krościenko nad Dunajcem ( od 1 października 1982 do 31 grudnia 1983 `` gmina Krościenko '' ) – gmina wiejska w Polsce położona w województwie małopolskim , w powiecie nowotarskim .
W latach 1975–1998 gmina administracyjnie należała do województwa nowosądeckiego .
Siedziba gminy to Krościenko nad Dunajcem .
Według danych z 31 grudnia 2010 r. gminę zamieszkiwało 6569 osób .
Natomiast 31 marca 2011 r. było to 6646 osób .
