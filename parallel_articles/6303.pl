Powiat krośnieński ( Galicja )
Powiat krośnieński – dawny powiat kraju koronnego Królestwo Galicji i Lodomerii , istniejący w latach 1867-1918 .
Siedzibą c.k .
starostwa było Krosno .
Powierzchnia powiatu w 1879 roku wynosiła 9,4894 mil kw .
( 546,02 km² ) , a ludność 64 842 osób .
Powiat liczył 84 osady , zorganizowane w 82 gminy katastralne .
Na terenie powiatu działały 2 sądy powiatowe - w Krośnie i Dukli .
