Krucjata Wyzwolenia Człowieka
Krucjata Wyzwolenia Człowieka lub KWC – program krzewienia abstynencji zainicjowany przez księdza Franciszka Blachnickiego i Ruch Światło-Życie , ogłoszony 8 czerwca 1979 roku w Nowym Targu w obecności Jana Pawła II .
Historia .
Pierwowzorem Krucjaty Wyzwolenia Człowieka jest Krucjata Trzeźwości ( później Krucjata Wstrzemięźliwości ) , założona przez ks .
Franciszka Blachnickiego w 1957 r. Centrala Krucjaty została zlikwidowana w 1960 r. przez Służbę Bezpieczeństwa .
W następnych latach propagowanie abstynencji odbywało się w ramach rekolekcji oazowych .
Bezpośrednim bodźcem do powołania KWC było wezwanie Jana Pawła II , aby przeciwstawiać się „ wszystkiemu co uwłacza ludzkiej godności i poniża obyczaje zdrowego społeczeństwa ” .
Założenia .
KWC jest programem ewangelizacyjnym , którego założeniem jest uwolnienie człowieka od uzależnień , przede wszystkim od uzależnienia alkoholowego , poprzez zawierzenie Jezusowi Chrystusowi .
Celem jest osiągnięcie wolności rozumianej zgodnie z chrześcijańską koncepcją wolności osoby .
Uczestnicy Krucjaty praktykują modlitwę , post i jałmużnę .
Modlitwa ofiarowana jest w intencji osób uzależnionych ; post oznacza dobrowolną rezygnację z wszelkich napojów alkoholowych , jako wyraz chrześcijańskiej miłości do ludzi uzależnionych ; jałmużna zaś jest dawaniem bliźnim tego , czego najbardziej potrzebują ( nadziei , troski , czasu ... ) .
Ideą Krucjaty jest odkrycie ludzkiej i chrześcijańskiej godności i życie według Bożego planu .
