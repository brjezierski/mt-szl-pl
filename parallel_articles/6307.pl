Krupnik ( miejscowość )
Krupnik ( bułg .
`` Крупник '' ) – wieś w południowo-zachodniej Bułgarii , w obwodzie Błagojewgrad , w gminie Simitli .
Według danych Narodowego Instytutu Statystycznego , 31 grudnia 2011 roku wieś liczyła 2067 mieszkańców .
Położenie .
Wzdłuż Strumy , w Kresneńskiego jaru .
Historia .
W XV wieku ze względu na rozwój gospodarczy Krupnik stał się centrum eparchii Krupniszkiej .
W XVII wieku nastąpiła islamizacja osady , ale jej mieszkańcy zachowali język bułgarski .
Neofit Riłski i Stefan Strezow opisywali wieś .
W czasie wojen bałkańskich odbyła się bitwa pod Krupnikiem .
