Kaszanka
Kaszanka ( kiszka , śl .
krupniok , kaszok ) – wyrób wędliniarski z kaszy i krwi oraz podrobów : wątroby , płuc , ozorów , skórek wieprzowych , tłuszczu .
W Polsce krupnioki śląskie uzyskały w czerwcu 2016 europejski certyfikat Chronione Oznaczenie Geograficzne .
Wyrób ten popularny jest także w kuchni słowackiej , węgierskiej i niemieckiej .
Charakterystyka wyrobu .
Kaszankę w warunkach domowych wytwarza się głównie przy okazji świniobicia , kiedy jest dostępna świeża krew .
Podstawowe przyprawy to cebula , pieprz i majeranek .
Kaszanka jest koloru ciemnobrązowego ( od krwi i kaszy ) i ma najczęściej osłonkę z jelita .
W zależności od regionu i producenta wytwarzane są produkty grubsze lub cieńsze .
Krupniok zazwyczaj ma kształt typowej kiełbasy grubości do 5 cm .
Pierwsze w Polsce „ Święto Krupnioka ” odbyło się 28 maja 2011 w Nikiszowcu , zabytkowej dzielnicy Katowic .
