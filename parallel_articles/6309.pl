Krupsko
Krupsko ( ukr .
Крупське ) – wieś na Ukrainie .
Należy do rejonu mikołajowskiego w obwodzie lwowskim , nad Dniestrem ; liczy 1494 mieszkańców .
Historia .
Stanisław Beydo Rzewuski herbu Krzywda przez pewien czas posiadał wieś Krupsko w powiecie żydaczowskim za I Rzeczypospolitej .
W II Rzeczypospolitej do 1934 roku wieś stanowiła samodzielną gminę jednostkową w powiecie żydaczowskim w woj .
stanisławowskim .
W związku z reformą scaleniową została 1 sierpnia 1934 roku włączona do nowo utworzonej wiejskiej gminy zbiorowej gminy Rozdół w tymże powiecie i województwie .
Po wojnie wieś weszła w struktury administracyjne Związku Radzieckiego .
W Krupsku urodziła się Tekla Juniewicz , superstulatka .
