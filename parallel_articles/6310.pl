Kruszwica
Kruszwica – miasto w Polsce , w województwie kujawsko-pomorskim , położone na Kujawach , w powiecie inowrocławskim , siedziba gminy miejsko-wiejskiej Kruszwica .
Miasto należy do Związku Miast Polskich .
Miasto położone jest na Pojezierzu Gnieźnieńskim , na północnym brzegu jeziora Gopło , w miejscu wypływania z niego rzeki Noteci .
Miasto królewskie położone w II połowie XVI wieku w powiecie kruszwickim województwa brzeskokujawskiego , należało do starostwa kruszwickiego .
W czasach zaborów miasto nosiło nazwę .
W latach 1975–1998 miasto administracyjnie należało do województwa bydgoskiego .
Według danych GUS z 31 grudnia 2020 r. Kruszwica liczyła 8709 mieszkańców .
W 2022 roku miasto obchodziło jubileusz 600-lecia nadania praw miejskich .
Kruszwica jest ważnym ośrodkiem gospodarczym , handlowym i kulturalnym regionu .
Znajdują się tu zakłady przemysłu spożywczego .
Ogromną rolę w życiu miasta odgrywa turystyka , związana z początkami polskiej państwowości .
Położenie .
Kruszwica jest położona w południowej części województwa kujawsko-pomorskiego w powiecie inowrocławskim nad jeziorem Gopło .
Gmina Kruszwica graniczy z 8 gminami .
