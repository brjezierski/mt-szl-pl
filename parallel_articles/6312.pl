Kruszynki
Kruszynki – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie brodnickim , w gminie Brodnica .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa toruńskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyły 432 mieszkańców .
Są szóstą co do wielkości miejscowością gminy Brodnica .
