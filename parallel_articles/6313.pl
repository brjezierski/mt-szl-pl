Kruszówiec
Kruszówiec – wieś w Polsce położona w województwie mazowieckim , w powiecie otwockim , w gminie Wiązowna .
Wieś jest sołectwem w gminie Wiązowna .
Wieś szlachecka Kruszowiec położona była w drugiej połowie XVI wieku w powiecie garwolińskim ziemi czerskiej województwa mazowieckiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa warszawskiego .
