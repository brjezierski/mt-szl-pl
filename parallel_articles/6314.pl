Krutyń
Krutyń ( / ) – wieś w Polsce , położona w województwie warmińsko-mazurskim , w powiecie mrągowskim , w gminie Piecki .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
Historia .
Wieś powstała około roku 1500 jako osada przy stanicy myśliwskiej wielkiego mistrza krzyżackiego Fryderyka z Miśni .
Stanicę myśliwską przebudowano następnie na drewniany zameczek , który w 1527 roku służył za schronienie księciu Albrechtowi Hohenzollernowi w czasie panującej w Prusach Książęcych epidemii dżumy .
Około 1651 zameczek był siedzibą nadleśniczego .
Wokół niego powstała osada smolarzy , dziegciarzy i robotników wyrabiających potaż .
