Krutynia
Krutynia ( niem .
`` Kruttinna '' ) – rzeka na Pojezierzu Mazurskim , wpływa do Jeziora Bełdany .
Szlak kajakowy .
Krutynia płynie przez rezerwat krajobrazowo-wodno-leśny położony na terenie trzech gmin : Piecki , Mikołajki i Ruciane-Nida .
Jej długość to 99 km , a powierzchnia dorzecza wynosi 638 km² .
Rzeka .
Dolina rzeki jest w wielu miejscach zatorfiona i zabagniona , wahająca się w granicach od około 1,5 km w części środkowej do 30-50 m w miejscach północnej i południowej części , otoczona jest wzniesieniami morenowymi o urozmaiconej rzeźbie .
Nurt rzeki Krutyni jest wolny , a rzeka tworzy liczne zakola , szczególnie w środkowej , zabagnionej części rezerwatu .
Spadek rzeki na długości około 10 km rezerwatu wynosi 1,3 m. Szerokość rzeki osiąga 30-40m , a głębokość od 1,5–2,5 m do 3–7 m .
Bieg Krutyni pod względem hydrologicznym rozpoczyna się na terenach otaczających Jezioro Warpuńskie .
Na pojezierzach jednak zwyczajowo przyjmuje się nieco inne kryteria nazewnictwa .
Rzeki zmieniają nazwy zawsze ( lub prawie zawsze ) wtedy , gdy przepływają przez jezioro , które zbiera wodę z innych , dodatkowych rzek i cieków .
Według takiego kryterium Krutynia zaczyna się w Jeziorze Krutyńskim , a kończy w jeziorze Bełdany , gdzie wpływa do Systemu Wielkich Jezior Mazurskich .
Zatem Krutynia w swoim różnym biegu przyjmuje różne nazwy .
