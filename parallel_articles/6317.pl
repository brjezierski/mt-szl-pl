Język kryzyjski
Język kryzyjski ( nazwa własna КърыцIаь мез ) – język Kryzów , zamieszkujących północną część Azerbejdżanu .
Należy do zespołu samurskiego , podgrupy dagestańskiej w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Język ten używany jest według różnych szacunków przez od ok. 6 tys .
osób ( 1975 ) do nawet 15 tys. , w osiedlu Kryz w północnym Azerbejdżanie oraz w kilku innych niewielkich wioskach w okolicy .
Nazwa języka wywodzi się od nazwy największej wsi , w której zamieszkują użytkownicy języka .
Język kryzyjski genetycznie najbliżej spokrewniony jest z językiem buduchyjskim .
Wśród języków zespołu samurskiego , kryzyjski wyróżnia się stosunkowo rozbudowanym wokalizmem oraz ograniczonym użyciem spółgłosek abruptywnych .
Językoznawcy wyróżniają trzy dość różniące się między sobą dialekty języka kryzyjskiego : kryzyjski , chaputlijski oraz dżekijski , a także kilka drobniejszych gwar lokalnych .
Różnice między poszczególnymi dialektami zasadniczo nie uniemożliwiają porozumienia między ich użytkownikami .
Język ten nie wykształcił piśmiennictwa .
Jest używany wyłącznie w sytuacjach nieformalnych , w domu , wśród przyjaciół .
W charakterze języka literackiego używany jest język azerski , jako język urzędowy Azerbejdżanu .
Azerski wywarł też znaczny wpływ język na kryzyjski , głównie w leksyce , ale także w fonetyce .
