Język krymczacki
Język krymczacki ( кърымчах тыльы ) – mowa z rodziny języków ałtajskich , używana przez Krymczaków .
Często uznawana za dialekt języka krymskotatarskiego .
Powstał na podstawie języka Tatarów krymskich z dodatkiem zapożyczeń z języka hebrajskiego .
Dawniej był zapisywany alfabetem hebrajskim , od lat 30 .
XX w. był zapisywany zmodyfikowanym alfabetem łacińskim , a obecnie jest zapisywany cyrylicą .
Większość Krymczaków zginęła w czasach Holocaustu .
W maju 1944 wielu Krymczaków wysiedlono wraz z Tatarami krymskimi do Uzbekistanu , gdzie część Krymczaków mieszka do dzisiaj .
Obecnie język krymczacki jest zagrożony wymarciem ( w 2001 było około 785 Krymczaków na Ukrainie ) .
