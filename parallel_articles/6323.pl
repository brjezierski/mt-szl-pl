Język krymskotatarski
Język krymskotatarski ( krymskotat .
, , , ) , język krymski ( krymskotat .
, , , ) – język Tatarów Krymskich , tradycyjnie zaliczany w skład podgrupy kipczacko-połowieckiej kipczackiej grupy języków tureckich .
Liczebność i rozkład geograficzny .
Językiem krymskotatarskim posługuje się na co dzień około 350 tysięcy osób , w tym około 250 tysięcy na Krymie .
W dalszej kolejności mówiący tym językiem zamieszkują Turcję i Azję Środkową , głównie Uzbekistan .
Istotna diaspora posługująca się tym językiem zamieszkuje w Rumunii i Bułgarii – łącznie nie więcej niż 30 tysięcy .
