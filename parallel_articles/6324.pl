Bowling
Bowling ( ang .
`` ten-pin bowling '' ) – rodzaj gry w kręgle .
Jest to najpopularniejsza odmiana gry w kręgle .
Została stworzona w oparciu o kręgle klasyczne .
W odmianie tej występuje dziesięć pinów ustawionych w trójkąt .
Gracz ma za zadanie strącić je w jak najmniejszej liczbie rzutów .
Aby strącić je wszystkie w pierwszym rzucie należy uderzyć kulą między pierwszy a trzeci pin ( kręgiel ) – w przypadku graczy praworęcznych .
Między pierwszy a drugi pin dla osób leworęcznych .
