Kręgowce
Kręgowce ( `` Vertebrata '' , od łac .
`` vertebra '' – kręg ) – najliczniejszy podtyp strunowców ( `` Chordata '' ) , mocno zróżnicowany morfologicznie ; obejmujący kręgouste , ryby , płazy , gady , ptaki i ssaki .
Dotychczas opisano około 58 000 gatunków kręgowców , co stanowi około 5 % opisanych gatunków zwierząt .
Wielkość współcześnie żyjących kręgowców waha się od 7,7 mm u żab `` Paedophryne amauensis '' do 33,9 m u płetwala błękitnego .
Cechują się obecnością tkanki kostnej , mają dwuboczną symetrię ciała z dobrze wyodrębnioną głową .
Szkielet wewnętrzny stanowi podporę dla tkanek i narządów w trakcie rozwoju , umożliwiając osiąganie dużych rozmiarów .
Charakterystyczną cechą kręgowców jest posiadanie czaszki oraz dwóch par kończyn .
Układ mięśniowy składa się z dwóch mas mięśni położonych równolegle po bokach kręgosłupa .
Ruch odbywa się dzięki skurczom mięśni , przyczepionych do kości lub chrząstek .
Kręgowce zamieszkują najróżniejsze środowiska , występują na wszystkich kontynentach i oceanach .
Liczne gatunki żyją w środowiskach synantropijnych , na których zwierzęta mają bezpośredni kontakt z działalnością człowieka ( pola , sady , miasta ) .
Ewolucja .
Pierwsze kręgowce pojawiły się na Ziemi około 525 mln lat temu , podczas okresu zwanego eksplozją kambryjską i wywodzą się z wczesnych bezczaszkowców .
Cechami świadczącym o ich bliskim pokrewieństwie z kręgowcami są m.in .
