Krypy ( gmina Wierzbno )
Krypy – wieś w Polsce położona w województwie mazowieckim , w powiecie węgrowskim , w gminie Wierzbno .
Nazwa wsi odnotowana została po raz pierwszy w 1432 r. i stanowi nazwę osobową `` Krypa '' w liczbie mnogiej .
N. os .
`` Krypa '' pochodzi od wyrazu pospolitego `` krypa '' – `` żłób , czółno '' , być może wywodzącego się z śrwniem .
nienotowanego `` kripp-tuoch '' , dziś niem `` Krippe '' – `` żlób '' .
W XVI wieku dla wsi funkcjonowała nazwa Krypy-Mścichy , drugi człon także od n. os .
– Mścich ( od Mścisław lub innego imienia na `` Mści- '' ) , i także w l. mnogiej .
W latach 1975–1998 miejscowość należała administracyjnie do województwa siedleckiego .
