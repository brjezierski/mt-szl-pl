Powiat ( Austria )
Powiaty w Austrii ( , `` politischer Bezirk '' ) – jednostka podziału administracyjnego i samorządu terytorialnego II stopnia w Austrii .
Jednostkami nadrzędnymi są kraje związkowe , podrzędnymi gminy .
Obecnie w Austrii istnieją 94 powiaty i 15 miasta statutarne ( `` Statutargemeinden '' , `` Statutarstädte '' ) , które posiadają osobny statut i funkcjonują zarówno na prawach przysługującym gminom , jak i powiatom .
Ponadto stołeczny Wiedeń dzieli się na 23 dzielnic .
Organem administracyjnym powiatu jest Bezirkshauptmannschaft ( `` komisja powiatowa '' ) , na którego czele stoi komisarz powiatowy ( – jeśli jest nim mężczyzna lub `` Bezirkshauptfrau '' – jeśli jest nią kobieta ) .
Osoby te nie są wybierane bezpośrednio przez obywateli , lecz są wyznaczane przez rząd kraju związkowego .
Jego zadaniem jest nadzór nad samorządem gminnym .
W stosunku do gmin jest organem wyższego stopnia w rozumieniu procedury administracyjnej .
Na szczeblu powiatowym nie są zawiązywane wspólnoty .
