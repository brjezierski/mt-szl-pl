Krystyna Bochenek
Krystyna Maria Bochenek z domu Neuman ( ur .
30 czerwca 1953 w Stalinogrodzie , zm .
10 kwietnia 2010 w Smoleńsku ) – polska dziennikarka i polityk , senator V , VI i VII kadencji ( 2004–2010 ) , wicemarszałek Senatu VII kadencji .
Działalność zawodowa .
Ukończyła I Liceum Ogólnokształcące w Katowicach , a następnie studia na Wydziale Filologii Polskiej Uniwersytetu Śląskiego .
Od 1976 była dziennikarką Polskiego Radia Katowice .
Przez wiele lat współpracowała z prasą i telewizją .
Zajmowała się upowszechnianiem kultury języka polskiego i popularyzacją zdrowia .
Była inicjatorką Ogólnopolskiego Dyktanda , największej w kraju i organizowanej także poza jego granicami imprezy promującej język polski .
Przygotowała blisko tysiąc godzin programów o zdrowiu , w tym radiowy `` Magazyn Medyczny '' .
Była również współautorką programu telewizyjnego `` Od A do Zdrowia '' .
Wchodziła w skład Rady Języka Polskiego przy Prezydium Polskiej Akademii Nauk ( w tym jako przewodnicząca Komisji Języka w Mediach ) .
W 2005 była inicjatorką uchwały Senatu w sprawie ustanowienia następnego roku Rokiem Języka Polskiego .
Organizowała konferencje naukowe , tj .
`` Język polskiej legislacji '' , `` Język polskich polityków początku XXI wieku '' , a także `` Festiwal Języka Polskiego 2006 '' , który odbył się pod patronatem marszałka Senatu Bogdana Borusewicza ( 5 tysięcy uczestników ) .
Stała na czele rady Śląskiego Funduszu Stypendialnego .
Pełniła funkcję wiceprzewodniczącej rady programowej TVP Polonia .
Była animatorką licznych wydarzeń medialnych i akcji charytatywnych na Śląsku , w tym kwesty `` Mamo nie płacz '' ( 1992 ) – na rzecz ratowania Oddziału Hematologii Dziecięcej w Zabrzu .
We współpracy ze śląskimi redakcjami prowadziła akcję profilaktyki raka piersi u kobiet pod nazwą `` Różowa wstążka .
Katowice rynek 2000 '' .
Współtworzyła pierwszą `` Wampiriadę '' ( niekonwencjonalną akcję honorowego oddawania krwi ) oraz akcję `` Rzuć palenie '' .
Prowadziła wiele licytacji na cele społeczne wspomagając m.in .
Wielką Orkiestrę Świątecznej Pomocy , odbudowę spalonego gmachu Opery Śląskiej , Teatr Śląski i Fundację Rodzin Górniczych .
