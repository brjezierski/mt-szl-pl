Krynica-Zdrój ( stacja kolejowa )
Krynica-Zdrój – stacja kolejowa w Krynicy-Zdroju , w województwie małopolskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca turystycznego .
W roku 2017 stacja obsługiwała 100–149 pasażerów na dobę .
Linie kolejowe .
Stacja jest ostatnią na linii kolejowej nr 105 , ma charakter stacji czołowej .
Infrastruktura .
Budynek dworca .
W budynku dworca działała kasa biletowa spółki Przewozy Regionalne i kasy przewoźników autobusowych , część budynku wykorzystywana na cele niezwiązane z transportem .
