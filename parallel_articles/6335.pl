Krzekotów
Krzekotów – wieś w Polsce położona w województwie dolnośląskim , w powiecie głogowskim , w gminie Głogów .
Leży około 8 km ( 5 mil ) na północny wschód od Głogowa i 88 km ( 55 mil ) na północny zachód od stolicy regionu Wrocławia .
Podział administracyjny .
W latach 1975–1998 miejscowość położona była w województwie legnickim .
