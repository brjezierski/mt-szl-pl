Krzesk-Królowa Niwa
Krzesk-Królowa Niwa – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Zbuczyn .
Do 1954 roku istniała gmina Królowa Niwa .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Z Krzeska pochodził Jan Pasiak [ 1914-1962 ] ps .
`` Jawor '' syn Michała i Franciszki z Jagielaków – komendant IV Okręgu Lublin Batalionów Chłopskich .
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Częstochowskiej w Krzesku-Majątku .
We wsi działa założona w 1925 roku jednostka ochotniczej straży pożarnej , która od 2014 roku jest w strukturach krajowego systemu ratowniczo-gaśniczego .
Na wyposażeniu jednostki jest średni samochód ratowniczo-gaśniczny GBA 2,5/24/2,5 Mercedes Atego .
Zabytki .
Na terenie wsi znajdują się pozostałości obwałowań wczesnośredniowiecznego grodziska ( VII-XIII w. ) .
