Chrzest
Chrzest – w chrześcijaństwie obrzęd nawrócenia i oczyszczenia z grzechów , sprawowany na polecenie samego jego założyciela Jezusa Chrystusa ( por .
Mt 28,18–20 ) .
Będąc `` obmyciem wodą , któremu towarzyszy słowo '' ( Ef 5,26 ) stoi na początku drogi wiary każdego z uczniów Chrystusa , jest pierwszym z sakramentów wtajemniczenia chrześcijańskiego .
Deklaracja Światowej Rady Kościołów z 1982 roku wymienia następujące istotne elementy chrztu : wysłuchanie nauk Pisma Świętego dotyczących jego znaczenia , przyzywanie Ducha Świętego , wyrzeczenie się zła , wyznanie wiary w Trójcę Świętą , wykorzystanie wody .
Osoba ochrzczona ma również wyrazić przekonanie , że chrzest da jej nową tożsamość syna lub córki Bożej oraz że jako członek Kościoła jest powołana , aby głosić Ewangelię .
Obrzęd ten `` wciela '' daną osobę w Chrystusa – ukrzyżowanego i zmartwychwstałego Pana – i łączy z ludem Bożym Nowego Przymierza .
Trynitarne rozumienie chrztu odrzucają wspólnoty religijne czerpiące z tradycji antytrynitaryzmu .
Etymologia i znaczenie słowa `` chrzest '' .
Pochodzenie polskiego słowa `` chrzest '' oraz pokrewnych słów w innych językach słowiańskich np .
`` křest '' w czeskim czy `` Крещение '' w rosyjskim nie jest całkiem jasne .
Wywodzi się je w ostatecznym rachunku od imienia Chrystusa ( łac .
`` Christus '' , greckie `` Khristos '' – od `` khrein '' – namaszczać ) .
Słowo `` chrzest '' nie jest więc przekładem greckiego i nowotestamentowego wyrazu `` báptisma '' , które w większości polskich przekładów Biblii tłumaczone jest na `` chrzest '' , a dosłownie znaczy „ zanurzenie ” .
Chrześcijanie wierzą , że jednoczy on z tajemnicą paschalną , męki , śmierci i zmartwychwstania Chrystusa dla uzyskania przebaczenia wszystkich grzechów i odrodzenia do nowego życia w Duchu Świętym , życia w wieczności .
Jest to obrzęd wyznania wiary , czyniący członkiem Kościoła-Ciała Chrystusa ( por .
Rz 11,9 ) .
Według katolickich historyków chrześcijaństwa J.Daniélou i I. Marrou rytuał chrztu chrześcijańskiego ma swe odległe korzenie w chrzcie jordańskim udzielanym przez Jana Chrzciciela mieszkańcom Jerozolimy ( por .
Łk 3,7 ) , bezpośrednie zaś we chrzcie Chrystusa w Jordanie .
Poza chrześcijaństwem , formą zewnętrznie podobną do chrztu są obmycia rytualne .
Do tej pory w judaizmie praktykowane jest obmycie z rytualnej nieczystości , chasydzi czynią to w specjalnie do tego przeznaczonym baseniku zwanym mykwą .
Obmycie rytualne jest praktykowane również w wielu innych religiach .
Chrzest w Biblii .
Używane w Nowym Testamencie na oznaczenie chrztu greckie słowo `` το βάπτισμα '' ( `` to baptisma '' ) ( Mt 21,25 /i par .
: Mk 11,30 oraz Łk 20,24/ ; Mk 1,4 ; 10,39 ; Łk 3,3 ; 7,29 ; Rz 6,4 ; Ef 4,5 i in .
) , pochodzące od greckiego czasownika `` βαπτίζειν '' , oznacza zanurzenie ciała w znaczeniu religijnym .
Jego duchowym skutkiem jest oczyszczenie , obmycie serca z brudów duchowych .
W odróżnieniu od niego , zwyczajne zanurzenie , bez znaczenia duchowego , oddawane jest przez autorów języka greckiego przez inne , podobne słowo : `` βάπτειν '' ( `` Baptein '' ) .
