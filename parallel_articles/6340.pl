Krzeszowice
Krzeszowice – miasto w południowej Polsce , w województwie małopolskim , w powiecie krakowskim , siedziba gminy miejsko-wiejskiej Krzeszowice .
Miasto jest jednym z ośrodków miejskich aglomeracji krakowskiej .
Krzeszowice należą do Jaworznicko-Chrzanowskiego Okręgu Przemysłowego .
Według danych GUS z 31 grudnia 2019 r. miasto miało 9975 mieszkańców .
