Krzewent
Krzewent – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie włocławskim , w gminie Kowal , pomiędzy trzema jeziorami : Rakutowskim , Lubiechowskim i Krzewenckim .
Folwark Krzewent , leżący w tzw .
`` Prusach Południowych '' nadany został w latach 1796-1797 generałowi majorowi von Blücherowi .
W latach 1975–1998 miejscowość administracyjnie należała do województwa włocławskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 214 mieszkańców .
Jest dziewiątą co do wielkości miejscowością gminy Kowal .
W Krzewencie mieszkał i zmarł aktor Jan Nowicki .
