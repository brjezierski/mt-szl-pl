Krzywiec ( powiat mrągowski )
Krzywiec ( dawniej niem .
`` Klein Krummendorf '' ) - osada w woj .
warmińsko-mazurskim , w pow .
mrągowskim , w gminie Mrągowo .
Osada położona na wschodnim brzegu jeziora Krzywego , przy drodze ze wsi Krzywe do wsi Brejdyny .
Historia .
Osada powstała w 1838 r. jako wybudowanie w obrębie gruntów wsi Krzywe .
W 1900 r. był to folwark , należący do majątku ziemskiego Szklarnia ( `` Glashutte '' ) i należał `` Wilhelma Wegnera '' .
Specjalizował się w hodowli bydła i posiadał gorzelnię .
W 1973 r. Krzywiec należał do sołectwa Krzywe .
