Krzyżkowice ( Pszów )
Krzyżkowice ( niem .
`` Krzischkowitz '' ) – dzielnica Pszowa .
Dawniej siedziba gminy Krzyżkowice .
13 października 1954 stały się integralną częścią Pszowa w związku z nadaniem gromadzie Pszów statusu miatsa .
W latach 1975-1994 Krzyżkowice były dzielnicą Wodzisławia Śląskiego .
Położenie i opis .
Jest to północno-zachodnia dzielnica miasta Pszów , przy drodze wojewódzkiej nr 933 .
Dzielnica ta ma charakter mieszkaniowy z zabudową jednorodzinną .
W Krzyżkowicach znajduje się Kościół pw .
Najświętszego Serca Pana Jezusa , Szkoła Podstawowa nr 3 , Publiczne Przedszkole nr 4 , filia Biblioteki Miejskiej ( budynki przy ulicy Armii Krajowej ) , przy ulicy Oskara Kolberga jest Ochotnicza Straż Pożarna w Krzyżkowicach oraz boisko , na którym swoje mecze rozgrywa LKS „ Naprzód 37 ” Krzyżkowice .
Przy ulicy Dworskiej znajdują się zabudowania dworu z I połowy XIX w .
Dzielnica sąsiaduje z : gminą Kornowac , gminą Lubomią , Kalwarią Pszowską , Rydułtowami i Pszowskimi Dołami .
Nazwa .
Według niemieckiego językoznawcy Heinricha Adamy ’ ego nazwa miejscowości pochodzi od polskiego słowa - `` krzyż '' .
W swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 r. we Wrocławiu jako starszą od niemieckiej wymienia on nazwę w obecnej polskiej formie `` Krzyzkowice '' podając jej znaczenie `` Kreuzenort '' czyli po polsku `` Krzyżowa miejscowość '' .
Niemcy zgermanizowali nazwę na `` Krzischkowitz '' w wyniku czego utraciła ona swoje pierwotne znaczenie .
