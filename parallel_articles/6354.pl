Krzemienica ( Białoruś )
Krzemienica ( także Krzemienica Kościelna , biał .
Крамяніца ) – wieś na Białorusi w rejonie zelwieńskim obwodu grodzieńskiego .
Położona nad rzeczką Krzemienicą , dopływem Zelwy .
Po roku 1919 w województwie białostockim II RP .
Jesienią 1939 roku zajęta przez wojska Rosji Sowieckiej .
Po roku 1945 w obwodzie grodzieńskim ZSRR .
Historia .
Najstarsza wzmianka o miejscowości pochodzi z 1498 roku , kiedy Mikołaj Raczkowicz namiestnik koniewski i słubicki , oraz jego bracia Jan , Paweł i Szymko otrzymali od króla Aleksandra Jagiellończyka prawo własności dóbr Krzemienica , które posiadali wcześniej ich dziad Raczko i ojciec Jundziłł .
