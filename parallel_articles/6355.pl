Krzyszkowo
Krzyszkowo – wieś w Polsce położona w województwie wielkopolskim , w powiecie poznańskim , w gminie Rokietnica .
Wieś szlachecka Krziszkowo położona była w 1580 roku w powiecie poznańskim województwa poznańskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
