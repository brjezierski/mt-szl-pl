Krzysztof Hanke
Krzysztof Alojzy Hanke ( ur .
31 października 1957 w Świętochłowicach ) – polski aktor , satyryk , artysta kabaretowy , lider Kabaretu Rak .
Życiorys .
Wczesne lata .
Pracował przez sześć miesięcy w Kopalni Węgla Kamiennego Bielszowice , następnie odbył służbę wojskową .
Jest absolwentem III Liceum Ogólnokształcącego w Zabrzu .
.
Kariera aktorska .
Na przełomie 1981 i 1982 wraz z Ryszardem Siwkiem i Andrzejem Stefaniukiem założył Kabaret Rak .
W latach 1999–2008 wcielał się w główną rolę Huberta „ Bercika ” Dwornioka w serialu TVP2 `` Święta wojna '' .
Inne przedsięwzięcia .
Wystąpił w teledyskach : „ Helka ” ( 2009 ) disco polowej grupy Chanel i „ Czerń i biel ” ( 2018 ) zespołu muzycznego Ich Troje .
Brał udział w programach rozrywkowych : `` Jak oni śpiewają '' ( 2009 ) i `` Lepiej późno niż wcale '' ( 2018 ) .
Ponadto prowadził program `` Gala śląskiej piosenki '' .
Życie prywatne .
Syn Henryka i Anny .
Wychowywał się w Bielszowicach .
Ma brata Zbigniewa .
Jest żonaty , ma dwoje dzieci : syna Aleksandra i córkę Urszulę .
Ma wnuki Leona ( ur .
2012 r. ) , Ninę ( ur .
2018 r. ) i Klarę ( ur .
2019 r. ) .
