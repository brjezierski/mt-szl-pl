Krzysztof Putra
Krzysztof Jakub Putra ( ur .
4 lipca 1957 w Józefowie , zm .
10 kwietnia 2010 w Smoleńsku ) – polski mechanik i polityk .
Wicemarszałek Senatu VI kadencji w latach 2005–2007 i wicemarszałek Sejmu VI kadencji w latach 2007–2010 .
Poseł na Sejm X , I i VI kadencji ( 1989–1993 , 2007–2010 ) i senator VI kadencji ( 2005–2007 ) , w 2004 przewodniczący Sejmiku Województwa Podlaskiego .
Życiorys .
Miał wykształcenie średnie , ukończył Technikum Mechaniczne w Białymstoku .
Działał w branżowych związkach zawodowych .
Od 1975 do 1994 pracował jako robotnik w Fabryce Przyrządów i Uchwytów w Białymstoku , od 1985 do 1986 pełnił funkcję wiceprzewodniczącego rady pracowniczej FPiU , a w okresie 1987–1988 jej przewodniczącego .
Od sierpnia 1980 należał do Niezależnego Samorządnego Związku Zawodowego „ Solidarność ” .
Sprawował mandat posła na Sejm X kadencji z ramienia Komitetu Obywatelskiego „ Solidarność ” , a w latach 1991–1993 z listy Porozumienia Obywatelskiego Centrum .
W 1990 objął funkcję prezesa zarządu wojewódzkiego Porozumienia Centrum .
W 2001 był jednym z współzałożycieli Prawa i Sprawiedliwości , przed śmiercią sprawował funkcję prezesa PiS regionu podlaskiego .
W latach 2002–2005 zasiadał w sejmiku podlaskim II kadencji , od maja do czerwca 2004 pełnił funkcję jego przewodniczącego .
W wyborach parlamentarnych w 2005 został wybrany na senatora w okręgu podlaskim .
Od 27 października 2005 do 4 listopada 2007 pełnił funkcję wicemarszałka Senatu .
W wyborach parlamentarnych w 2007 uzyskał mandat poselski , otrzymując w okręgu podlaskim 53 651 głosów .
