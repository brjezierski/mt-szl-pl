Krzysztof Respondek
Krzysztof Respondek ( ur .
17 lipca 1969 w Tarnowskich Górach ) – polski aktor , piosenkarz i artysta kabaretowy .
Życiorys .
Pochodzi z Miasteczka Śląskiego .
Jest absolwentem II LO im .
Stanisława Staszica w Tarnowskich Górach .
W 1992 ukończył studia na Wydziale Aktorskim PWST we Wrocławiu ( wydziały zamiejscowe krakowskiej PWST ) .
W latach 1992–1999 był związany z Teatrem Rozrywki w Chorzowie , gdzie występował na ogół w musicalach i miał możliwość współpracować m.in .
z takimi reżyserami , jak Józef Szajna , Marcel Kochańczyk , Wojciech Kępczyński .
1 stycznia 1999 dołączył do artystów kabaretu Rak , w którym wygłasza monologi , śpiewa piosenki oraz bierze udział w skeczach .
Zwycięzca 3. edycji `` Jak oni śpiewają '' , z którego wszystkie nagrody przeznaczył na cele charytatywne .
