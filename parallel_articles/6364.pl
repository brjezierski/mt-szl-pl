Krasław ( stacja kolejowa )
Krasław ( , ) – stacja kolejowa w miejscowości Krāslavas stacija , w gminie Krasław , na Łotwie .
Położona jest na linii Witebsk - Dyneburg .
Nazwa pochodzi od pobliskiego miasta Krasław .
Obecnie jest to stacja końcowa dla pociągów pasażerskich z Rygi
Historia .
Stacja została otwarta w XIX w. na drodze żelaznej dynebursko-witebskiej , pomiędzy stacjami Malinówka i Baltyn .
Początkowo nosiła nazwę `` Krasławka '' .
