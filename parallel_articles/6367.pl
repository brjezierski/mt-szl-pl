Królowe ( film )
Królowe `` ( hiszp .
Reinas ) '' - hiszpańsko-włosko-holenderska komedia romantyczna , wyreżyserowana przez Manuela Gómeza Pereira .
Film był kręcony od 17 sierpnia do 21 października 2004 roku w Madrycie .
Opis fabuły .
W Hiszpanii właśnie zalegalizowano małżeństwa homoseksualne .
Kilka par młodych gejów chce się pobrać .
Przedtem jednak spotykają się wspólnie ze swoimi rodzicami w celu poznania przyszłych teściów .
Okazuje się , że nie wszyscy teściowie się polubili , a wręcz przeciwnie , niektórzy nawet są przeciwni małżeństwu swoich synów .
