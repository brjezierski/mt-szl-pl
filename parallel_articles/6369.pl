Żyrandol
Żyrandol ( z fr .
`` girandole '' ) – zazwyczaj elektryczne urządzenie oświetleniowe lub świecznik montowany do sufitu pomieszczenia .
Występuje zarówno w mieszkaniach , jak i w wielkogabarytowych salach , holach i widowniach .
Zawiera od jednego do bardzo wielu punktów świetlnych .
W pomieszczeniach bardzo wielkich może mieć skomplikowaną konstrukcję i składać się z wielkiej ilości elementów ozdobnych , często szklanych lub kryształowych .
Żyrandole były używane już w średniowieczu w kościołach , klasztorach i zamkach dla lepszego oświetlania większych sal .
Zazwyczaj miały postać drewnianego krzyża , przymocowanego linami do powały , na ramionach którego umieszczano świece .
