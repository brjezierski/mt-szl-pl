Siennica Królewska Duża
Siennica Królewska Duża – wieś w Polsce położona w województwie lubelskim , w powiecie krasnostawskim , w gminie Siennica Różana .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
Wieś stanowi sołectwo gminy Siennica Różana .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 452 mieszkańców .
