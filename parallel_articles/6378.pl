Wierzbnik ( województwo opolskie )
Wierzbnik , ( niem .
Herzogswalde ; daw .
`` Książyce '' , `` księżycowy las '' ) – wieś w Polsce położona w województwie opolskim , w powiecie brzeskim , w gminie Grodków .
W latach 1945-54 siedziba gminy Wierzbnik .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Od północy granicą wsi jest autostrada A4 .
W miejscowości znajduje się kościół parafialny pw .
św .
Michała Archanioła , przedszkole publiczne , budynek byłej szkoły podstawowej , świetlica wiejska , dwie kapliczki przydrożne , stary , być może późnośredniowieczny krzyż kamienny , oraz ruiny zamku .
W Wierzbniku działa koło gospodyń wiejskich , ochotnicza straż pożarna oraz dwa zakłady produkcji rolnej .
Historia .
Powstała na terenie leśnym prawdopodobnie w 1368 roku , jednak w dokumentach nazwa osady występuje już w roku 1305 .
Była własnością właścicieli z Pogorzeli .
W 1375 r. Jesko z Pogorzeli sprzedał swoją część grodkowskiemu kasztelanowi Peschko Rosenau .
