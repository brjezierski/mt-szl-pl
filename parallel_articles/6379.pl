Ksylofon
Ksylofon ( gr .
`` ξύλον '' 'drewno ' i `` φωνή '' 'dźwięk , głos ' ) , harmonika słomiana , gigelyra – instrument muzyczny z grupy idiofonów uderzanych , składający się z szeregu strojonych sztabek z twardego drewna najczęściej palisandru ( wibratorów , od których wielkości zależy wysokość dźwięku ) , ułożonych w jednym lub więcej rzędach i uderzanych pałeczkami .
Ksylofon dotarł do średniowiecznej Europy z Azji , a niezwykłą popularność zyskał w XIX w. dzięki kompozycjom Camille ' a Saint-Saënsa , który wprowadził ten instrument na stałe do orkiestry symfonicznej .
Wybitnym wirtuozem ksylofonu był w XIX w. Michał Józef Guzikow urodzony w Szkłowie na Białorusi .
Grał on na instrumencie 4-rzędowym ułożonym na słomianych wałkach i uderzanym drewnianymi pałeczkami w kształcie łyżek .
Taki typ ksylofonu był powszechny do lat 50 .
XX wieku .
We współczesnych ksylofonach płytki układa się w dwóch równoległych rzędach , a ich liczba dochodzi do pięćdziesięciu ; najczęściej spotykana rozpiętość tonalna to `` g '' 1- '' g '' 4 lub `` c '' ²- '' c '' 5 .
Instrumenty koncertowe posiadają metalowe rezonatory pod płytkami a same płytki wytwarzane są z włókna szklanego .
We współczesnej muzyce poważnej ksylofon pojawia się bardzo często .
Pełni też często funkcję ilustracyjną w muzyce filmowej .
Ksylofon jest wykorzystywany od początku XX w. w wychowaniu muzycznym dzieci , w przedszkolach i nauczaniu początkowym w szkole podstawowej .
Prekursorem tej metody był Carl Orff .
Skonstruował on prosty model ksylofonu 1-rzędowego o 1 lub 2 oktawach z wyjmowanymi łatwo płytkami .
Prostota instrumentu skłaniała dzieci do zabawy z muzyką , rozwijania ekspresji i inwencji twórczej .
