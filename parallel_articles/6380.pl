Chrzelice
Chrzelice ( dodatkowa nazwa w j. niem .
Schelitz , ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Biała .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na pograniczu Równiny Niemodlińskiej i Kotliny Raciborskiej , będących częścią Niziny Śląskiej .
Przepływa przez nią rzeka Biała .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 637 osób .
Nieoficjalnymi częściami wsi są Przedzamcze , Zidlungi , Stawiska , Słoneczno , Kochalisko i Sicie .
Do wsi należą również osady leśne Śródlesie ( SIMC 0491216 ) , Śródlesie ( SIMC 0491200 ) i Jeleni Dwór .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 16 km od granicy z Czechami , na pograniczu Równiny Niemodlińskiej i Kotliny Raciborskiej , tuż przy granicy gminy Biała z gminą Strzeleczki .
Należy do Euroregionu Pradziad .
Leży w międzyrzeczu Rzymkowskiego Rowu i rzeki Biała ( lewy dopływ Osobłogi ) .
