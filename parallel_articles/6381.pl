Kuala Lumpur
Kuala Lumpur ( z malajskiego „ błotniste ujście ” ) – stolica Malezji , licząca razem z przedmieściami ponad 7 milionów mieszkańców .
Kuala Lumpur jest siedzibą parlamentu Malezji .
Miasto było niegdyś siedzibą władzy wykonawczej i sądowniczej oddziałów rządu federalnego , ale przeniosły się one do Putrajaya w 1999 .
Niektóre sekcje sądownictwa pozostały w stolicy .
Oficjalna rezydencja króla Malezji , Istana Negara , znajduje się również w Kuala Lumpur .
Oceniane jest jako metropolia globalna ( Alfa ) .
Kuala Lumpur jest kulturalnym , finansowym i gospodarczym centrum Malezji , posiadającym kilka z największych centrów handlowych na świecie .
Miasto Kuala Lumpur jest położone w granicach Federalnego Terytorium Kuala Lumpur i jest jednym z trzech terytoriów federalnych Malezji .
