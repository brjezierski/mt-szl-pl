Kucborek
Kucborek ( dawniej niem .
`` Paulshof '' ) – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie chełmińskim , w gminie Papowo Biskupie .
Wieś zajmuje 267,68 hektarów powierzchni .
Historia .
Obszar miejscowości przez kilka wieków należał do Papowa Biskupiego .
Było to tak zwane wolne dobra sołtysie .
Kucborek w różnych źródłach historycznych figurował jako Kucbork ( folwark ; 1725 ) , Kuczborek al .
Papowo ( 1877 ) , Paulshof ( 1883 ) .
W 1887 roku wieś występowała pod nazwą Paulshof .
Właścicielami wsi byli wtedy : Michał Nawrocki , Andrychowicz ( od 1796 ) , Johann Menna ( od 1822 ) , Franz August Schutze ( od 1840 ) , Adolf Stoboy ( od 1843 ) , Carl Adolf Stoboy ( od 1850 ) , Hugo Meyer ( od 1892 ) .
Pod koniec XIX wieku Kucborek liczył 11 budynków , 4 domy .
Wieś zamieszkiwało wtedy 68 mieszkańców ( 36 katolików i 32 ewangelików ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa toruńskiego .
W 2013 roku na terenie Kucborka działał jeden podmiot gospodarczy .
