Kuchnia śląska
Kuchnia śląska – dział sztuki kulinarnej wywodzącej się i charakterystycznej dla Górnego Śląska i Ślązaków .
Przez wieki kultura śląska mieszała się i czerpała z wpływów Polski , Czech , Austrii i Niemiec .
Spowodowało to powstanie oryginalnej kultury z własnymi gwarami , strojami ludowymi oraz kuchnią .
Ze względu na wielonarodowościowy charakter tej krainy , tutejsza kuchnia jest niejednolita w swym charakterze .
Na Górnym Śląsku spożywane są potrawy typowo śląskie ( tu powstałe lub tylko tu spożywane ) oraz potrawy popularne również w krajach sąsiadujących , często znane tu pod śląską nazwą : np .
krepel ( pączek ) czy buchta ( kluski na parze ) .
Często spotykanym zjawiskiem jest przypisywanie Ślązakom jako dania regionalnego rolady wraz z kluskami śląskimi i modrą kapustą ( często jako pierwsze danie jest nudelzupa ) .
