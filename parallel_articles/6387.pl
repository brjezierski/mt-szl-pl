Siedzibą władz gminy był Kudelczyn , a następnie Przywózki .
Gmina Kudelczyn jest wymieniona jako jedna z 12 gmin wiejskich powiatu sokołowskiego guberni siedleckiej .
W 1919 roku gmina weszła w skład woj .
