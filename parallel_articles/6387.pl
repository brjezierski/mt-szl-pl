Po wojnie gmina zachowała przynależność administracyjną .
Według stanu z dnia 1 lipca 1952 roku gmina składała się z 20 gromad .
1 września 1952 roku dokonano wymiany części terenów gmin Kudelczyn i Grochów .
Gmina została zniesiona 29 września 1954 roku wraz z reformą wprowadzającą gromady w miejsce gmin .
Jednostki nie przywrócono 1 stycznia 1973 roku po reaktywowaniu gmin , a jej dawny obszar wszedł w skład gmin Sokołów Podlaski i Bielany .
