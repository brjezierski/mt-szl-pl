Kudowa-Zdrój ( stacja kolejowa )
Kudowa-Zdrój – stacja kolejowa w Kudowie-Zdroju , w powiecie kłodzkim , w województwie dolnośląskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca turystycznego .
Jest to stacja czołowa na linii Kłodzko Główne – Kudowa-Zdrój .
Dawniej linia prowadziła do czeskiego Náchodu .
W roku 2017 stacja obsługiwała 200–299 pasażerów na dobę .
Informacje ogólne .
W budynku dworcowym funkcjonuje poczekalnia , działająca nastawnia obsługiwana jest przez dyżurnego ruchu .
Budynek lokomotywowni pełni obecnie funkcję hurtowni budowlanej , dlatego nie popada w ruinę .
Obecnie nie prowadzi się obsługi ruchu towarowego na trasie .
Po bocznicach pozostały jedynie drobne ślady .
Historia .
Jedną z najważniejszych inwestycji w powiecie kłodzkim była trasa kolejowa wiodąca z Kłodzka do Kudowy-Zdroju , z możliwością przedłużenia do czeskiego Náchodu .
Prace przy budowie tej linii trwały bardzo długo ( blisko 20 lat ) ze względu m.in .
na trudne warunki terenowe .
Jako pierwszy uruchomiono odcinek do Szczytnej ( 1886-1890 ) .
Następnie 1 grudnia 1902 otwarto odcinek do Dusznik .
Do Kudowy-Zdroju kolej dotarła 10 lipca 1905 r .
Stacja Kudowa-Zdrój według projektu oprócz funkcji związanej z obsługą ruchu pasażerskiego i towarowego , miała obsługiwać przybywające parowozy .
Dlatego oprócz budynku stacyjnego z kasą i poczekalnią , magazynu towarowego i placu ładunkowego , wyposażono ją w budowle konieczne do obsługi taboru : lokomotywownia na 14 stanowisk , wieża ciśnień z żurawiem wodnym oraz basen węglowy , pozwalały one uzupełnić zapasy węgla i wody w przybyłych lokomotywach oraz dokonać koniecznych napraw .
Pierwsze zabudowania dworcowe i magazynowe okazały się zbyt małe , dlatego w 1912 roku oddano do użytku nowy duży budynek dworca , który pomieścił kasy , poczekalnię , restaurację i pomieszczenia dla pracowników kolejowych .
Od stacji odchodziły dwie prywatne bocznice kolejowe z czego jedna obsługiwała pobliskie zakłady bawełniane .
Po II wojnie światowej i przejściu ziemi kłodzkiej pod administracje polską przemianowano stację na `` Chudowa Zdrój '' , a potem na `` Kudowa-Zdrój '' .
Stacja pełni swoją funkcję do dzisiaj .
Połączenia .
Od 3 marca 2010 roku pociągi przestały kursować , z uwagi na zbyt niską prędkość szklakową ( 20 km/h ) .
Zastąpiono je komunikacją autobusową .
15 grudnia 2013 zakończono remont torowiska na odcinku Duszniki-Zdrój – Kudowa i pociągi powróciły na swoją regularną trasę a prędkość szlakowa wynosi 60 km/h .
