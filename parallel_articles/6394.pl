Województwo kujawsko-pomorskie
Województwo kujawsko-pomorskie – jedno z 16 polskich województw leżące w północno-centralnej części kraju .
Zostało utworzone na mocy ustawy z dnia 24 lipca 1998 w przybliżeniu z dawnych województw : bydgoskiego , toruńskiego i włocławskiego .
30 kwietnia 2022 województwo liczyło około 2 mln mieszkańców .
Urzędy i jednostki szczebla wojewódzkiego zostały rozdzielone pomiędzy dwa główne miasta regionu .
Siedziba wojewody oraz większości urzędów administracji państwowej została ustanowiona w Bydgoszczy , natomiast siedziba sejmiku województwa , marszałka i zarządu województwa oraz organów administracji samorządowej w Toruniu .
Patronem województwa jest św .
Jan Paweł II .
Święto regionu , ustanowione w 2008 , obchodzone jest 7 czerwca .
Historia .
Trzon województwa stanowią historyczne Kujawy , a ponadto ziemia chełmińska będąca częścią Pomorza Nadwiślańskiego , ziemia dobrzyńska oraz fragmenty Wielkopolski i Pomorza Gdańskiego .
W X–XII w. większość ziem obecnego regionu należała do państwa pierwszych Piastów w zasięgu prowincji mazowieckiej .
Jedynie północno-zachodnie obszary podlegały kontroli Pomorzan , którzy do początku XII w. posiadali warownie m.in .
w Nakle , Świeciu i Wyszogrodzie .
W końcu XII w. nastąpiło wydzielenie z Mazowsza dzielnicy kujawskiej , co zapoczątkowało kształtowanie się zrębu historycznego regionu , obejmującego również tereny zawiślańskie ( ziemia chełmińska i dobrzyńska ) .
