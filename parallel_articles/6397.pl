Kukle ( Białoruś )
Kukle ( ; ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie werenowskim , w sielsowiecie Misiewicze .
W dwudziestoleciu międzywojennym leżały w Polsce , w województwie nowogródzkim , w powiecie lidzkim , w gminie Zabłoć .
Po II wojnie światowej w granicach Związku Sowieckiego .
Od 1991 w niepodległej Białorusi .
