Kukułowo
Kukułowo ( niem .
`` Kucklow '' ) – wieś w północno-zachodniej Polsce , położona w województwie zachodniopomorskim , w powiecie kamieńskim , w gminie Kamień Pomorski , nad cieśniną Dziwną .
Przy południowej części wsi znajduje się ujście strugi Szczuczyny do Dziwny .
Znajdował się tu zamek , będący siedzibą kapituły i proboszczów kamieńskich .
Od 1628/30 aż do śmierci w roku 1640 w zamku , użyczonym mu przez księcia Bogusława XIV , mieszkał zdetronizowany w 1616 r. książę Kurlandii i Semigalii Wilhelm Kettler , którego syn Jakub Kettler w 1638 r. odzyskał księstwo kurlandzkie .
W latach 1946–1998 miejscowość administracyjnie należała do województwa szczecińskiego .
