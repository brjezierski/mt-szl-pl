Kuldyga
Kuldyga ( ; hist . )
– miasto w zachodniej Łotwie , w Kurlandii , nad rzeką Windawą , siedziba władz gminy Kuldyga , 12 123 mieszkańców .
Kuldyga do dziś zachowała swój rzemieślniczy charakter .
Znajdują się tam również nowoczesne zakłady przemysłowe , dominuje przemysł drzewny .
W mieście mają swoją siedzibę również filie wyższych uczelni pedagogicznych z Rygi i Lipawy .
Historia .
Kuldyga wzmiankowana była po raz pierwszy w 1245 roku , w dokumentach biskupa Wilhelma .
W tych latach na terenie obecnej Kuldygi zbudowano zamek inflanckiej gałęzi zakonu krzyżackiego nazwany Jesusburg ( później Goldingen ) .
W 1378 Kuldyga uzyskała prawa miejskie , a w 1398 wstąpiła do Związku Hanzeatyckiego .
W XVII wieku była , obok Mitawy , głównym ośrodkiem Księstwa Kurlandii i Semigalii .
Do dziś nazywana jest stolicą Kurlandii.We wrześniu 1659 roku tutejszy zamek został zajęty przez wojska Rzeczypospolitej .
Twierdza mająca 1500 ludzi ( 1/3 sił szwedzkich w Kurlandii ) zdobyta została przez około 800 żołnierzy , niedysponujących większą liczbą dział .
