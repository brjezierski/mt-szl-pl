Kumasi
Kumasi – stolica regionu Aszanti w Ghanie , znajdująca się na południu kraju , około 250 km na północny zachód od Akry .
Z powodu zróżnicowanej roślinności znana jest jako miasto ogród .
Około 32 kilometrów na południe leży największe naturalne jezioro w Ghanie , Bosumtwi .
Historia .
Miasto położone jest w zalesionym regionie zachodniej Afryki , ale są dowody na to , że obszary te były bezdrzewne w neolicie .
Kumasi nabrało rozgłosu w roku 1695 , gdy zostało stolicą państwa Aszantów ( tak zwanej konfederacji Aszanti ) , z powodu aktywności swojego władcy Osei Tutu ( władca Kumasi , znany jako kumasehene , był równocześnie królem konfederacji ) .
Część grodu , łącznie z pałacem królewskim , została zniszczona przez brytyjskie wojsko podczas drugiej wojny Brytyjczyków z Aszantami w roku 1874 .
Kumasi pozostaje miastem królewskim , chociaż odkąd Ghana zadeklarowała niepodległość w roku 1957 , rola króla jest głównie symboliczna .
