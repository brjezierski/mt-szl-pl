Kumycy
Kumycy , Kumykowie – grupa etniczna zamieszkująca Kaukaz Północny , a zwłaszcza nizinną część Dagestanu .
Posługują się językiem kumyckim , należącym do języków tureckich .
W XVI–XVII w. przyjęli islam sunnicki .
Przed przyłączeniem do Rosji w 1867 r. tworzyli księstwo tarkowskie .
Pod koniec lat 90. ich liczebność szacowana była na 200–280 tys .
