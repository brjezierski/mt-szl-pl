Kuna Yala
Kuna Yala – region autonomiczny ( comarca ) w północnowschodniej części Panamy zamieszkany przez Indian Kuna .
Powierzchnia : 2340,7 km² .
Ludność : 45 236 mieszkańców ( 2018 , szacowana ) .
Stolicą jest miasto El Porvenir położone na wyspie o tej samej nazwie .
Geografia .
W skład regionu wchodzi 365 wysp archipelagu San Blas oraz wąski pas wybrzeża Morza Karaibskiego o długości 373 kilometrów .
Od wschodu graniczy z Kolumbią , od południa z regionem autonomicznym Emberá-Wounaan , prowincją Darién i prowincją Panama , od zachodu z prowincją Colón .
Klimat tropikalny .
Etymologia .
Nazwa „ Kuna Yala ” .
w języku Kuna znaczy „ ziemia Kuna ” .
Poprzednia nazwa regionu to „ San Blas ” .
Gospodarka .
Głównymi gałęziami gospodarki są turystyka , rolnictwo , rybołówstwo i produkcja pamiątek .
Najsłynniejszym lokalnym produktem są tkaniny mola .
Ważnym źródłem dochodów są także transfery pieniężne od mieszkańców pracujących w Panamie i Colón .
Wskaźnik rozwoju społecznego HDI : 0,490 ( niski )
Historia .
Polityczna historia Kuna Yala rozpoczęła się od buntu ludu Kuna w 1925 roku , któremu przewodził wódz i szaman Nele Kantule .
Wówczas Kunowie przyjęli jako swój symbol flagę ze swastyką , która była w ich kulturze znanym symbolem .
W wyniku nacisków przywódców ludu Kuna , autonomia Kuna Yala została uznana przez Panamę w 1930 roku .
