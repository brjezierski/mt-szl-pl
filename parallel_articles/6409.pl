Kuoka krótkoogonowa
Kuoka krótkoogonowa ( `` Setonix brachyurus '' ) – gatunek ssaka z podrodziny kangurów ( Macropodinae ) w obrębie rodziny kangurowatych ( Macropodidae ) .
Zasięg występowania .
Kuoka krótkoogonowa występuje nieregularnie w południowo-zachodniej Australii Zachodniej od wschodniej części Perth do Stirling Range i Green Range na wschód od Albany ; także wyspy Rottnest i Bald .
Taksonomia .
Gatunek po raz pierwszy naukowo opisali w 1830 roku francuscy przyrodnicy Jean René Constant Quoy i Joseph Paul Gaimard nadając mu nazwę `` Kangarus brachyurus '' .
Jako miejsce typowe odłowu holotypu Quoy i Gaimard wskazali Zatokę Króla Jerzego , w Australii Zachodniej , w Australii .
Jedyny przedstawiciel rodzaju kuoka ( `` Setonix '' ) który opisał w 1842 roku francuski zoolog René Lesson .
Gatunek monotypowy .
Morfologia .
Długość ciała ( bez ogona ) samic 39–50 cm , samców 43–54 cm , długość ogona samic 23,5–28,5 cm , samców 25–31 cm ; masa ciała samic 1,6–3,5 kg , samców 2,7–4,2 kg .
Ekologia .
Kuoka krótkoogonowa zamieszkuje lasy tropikalne .
Prowadzi nocny , ziemny tryb życia , dzień spędzając w gęstych zaroślach .
Żyje w niedużych grupach rodzinnych .
Jest roślinożercą , jej futro jest gęste , długie , szarobrązowe na głowie , rude na karku .
Podczas kopulacji zostaje zapłodnionych wiele komórek jajowych , jednak tylko z jednej rozwija się zarodek .
Ciąża trwa około miesiąca , po porodzie młode pozostaje w torbie matki przez około pół roku .
Jeżeli nie przeżyje , z jednej z pozostałych zapłodnionych komórek rozwija się kolejny embrion .
Kuoki krótkoogonowe żyją około 5 lat .
Głównym zagrożeniem dla nich , prócz człowieka , są lisy .
