( 1503 ) Kuopio
( 1503 ) Kuopio – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 94 dni w średniej odległości 2,63 au .
Została odkryta 15 grudnia 1938 roku w Obserwatorium Iso-Heikkilä w Turku przez Yrjö Väisälä .
Nazwa planetoidy pochodzi od Kuopio , miasta w Finlandii .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 1503 ) 1938 XD .
