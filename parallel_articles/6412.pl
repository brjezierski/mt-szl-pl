Kupientyn-Kolonia
Kupientyn-Kolonia – kolonia w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Sabnie .
Leży nad Cetynią dopływem Bugu .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni wyznania rzymskokatolickiego zamieszkali w miejscowości należą do parafii Niepokalanego Poczęcia Najświętszej Maryi Panny w Niecieczy .
