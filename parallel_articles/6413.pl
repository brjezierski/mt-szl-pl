Budy Kupientyńskie
Budy Kupientyńskie – wieś w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Sokołów Podlaski .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni kościoła rzymskokatolickiego należą do parafii Miłosierdzia Bożego w Sokołowie Podlaskim
