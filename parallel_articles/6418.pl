Kurikka ( stacja kolejowa )
Kurikka ( fiń : `` Kurikan rautatieasema '' ) – nieczynna stacja kolejowa w Kurikka , w regionie Ostrobothnia Południowa , w Finlandii .
Znajduje się na linii Seinäjoki-Kaskinen .
Ruch pasażerski na linii został wstrzymany w 1960 .
Budynek dworca jest obecnie własnością prywatną i został odnowiony z poszanowaniem pierwotnego wyglądu .
Budynek teraz służy jako restauracja .
