Kurlandia
Kurlandia ( łot .
`` Kurzeme '' – ziemia Kurów , niem .
`` Kurland '' ) – kraina historyczna w zachodniej części Łotwy na półwyspie nad Bałtykiem .
Nazwa pochodzi od łotewskiego plemienia Kurów ( Kuronów ) .
W szerszym kontekście nazwa używana na określenie Księstwa Kurlandii i Semigalii .
Graniczy z Semigalią na wschodzie i Żmudzią na południu .
Historia .
Początkowo zamieszkiwana w większości przez ugrofińskich Liwów i przez mówiące językiem kurońskim bałtyckie plemię Kurów , którzy trudnili się na wielką skalę piractwem i handlem bałtyckim .
W 1230 , w celu uniknięcia rozlewu krwi , ich władca , Lammekinus , przyjął chrzest i zwierzchnictwo papieża .
W 1260 Żmudzini odnieśli tu zwycięstwo nad krzyżakami w bitwie pod Durben .
Ostatecznie w 1269 r. podbita przez niemiecki zakon kawalerów mieczowych , w większości stanowiła część jego państwa .
