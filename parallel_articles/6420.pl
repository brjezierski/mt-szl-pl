Kuropas
Kuropas ( niem .
`` Korpitz '' , 1936–1945 `` Korndorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
Nazwa .
W kronice łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej w latach 1295-1305 miejscowość wymieniona jest w zlatynizowanej formie `` Curopasch '' .
W alfabetycznym spisie miejscowości na terenie Śląska wydanym w 1830 roku we Wrocławiu przez Johanna Knie wieś występuje pod nazwą zgermanizowaną `` Korpitz '' oraz polską nazwą `` Kuroprascz '' we fragmencie `` Kuroprascz , nach herschel polnische Benennung von Korpitz '' .
Ze względu na polskie pochodzenie w okresie rządów hitlerowskich nazistowska administracja III Rzeszy zmieniła zgermanizowaną nazwę w 1936 na nową , całkowicie niemiecką – `` Korndorf ''
