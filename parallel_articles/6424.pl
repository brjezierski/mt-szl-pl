Kurt Alder
Kurt Alder ( ur .
10 lipca 1902 w Königshütte ( obecnie Chorzów ) , zm .
20 czerwca 1958 w Kolonii ) – profesor nauk chemicznych , laureat Nagrody Nobla z chemii w 1950 roku za odkrycie i badania nad reakcjami skoordynowanymi z udziałem dienów .
Życiorys .
Rodzice Kurta posiadali pochodzenie chłopskie .
Urodził się jako najstarsze dziecko Josepha i Marii .
Ojciec zajmował stanowisko nauczyciela mianowanego początkowo w szkole powszechnej nr III , a następnie nr V w Królewskiej Hucie .
Młodość spędził w Chorzowie .
Edukację szkolną rozpoczął 1 kwietnia 1908 roku w męskiej szkole powszechnej nr V , w której nauczał jego ojciec , następnie Wyższej Szkole Realnej o profilu matematyczno-przyrodniczym .
Rodzina Alderów przeżyła w Królewskiej Hucie okres powstań śląskich i plebiscytu .
Po zdaniu matury przez Kurta , w związku z podziałem pruskiej prowincji Śląsk , rodzina przeniosła się do Berlina , gdzie Alder rozpoczął studia na Uniwersytecie Humboldtów .
