Vonnegut urodził się 11 listopada 1922 r. w Indianapolis w bogatej rodzinie , jako najmłodszy z trójki rodzeństwa , syn architekta Kurta Vonneguta Sr. i Edith Lieber .
Obydwoje rodzice byli pochodzenia niemieckiego .
Krach na giełdzie w 1929 r. zmusił Vonnegutów do zmiany stylu życia , zwolnienia służby i wyprowadzenia się z drogiego mieszkania .
Matka przeżyła to bardzo boleśnie , wpadając w depresję .
Vonnegut uczęszczał do Orchard School , a następnie do Shortridge High School , gdzie pisał do szkolnej gazety „ The Shortridge Echo ” .
W 1940 r. , za namową brata , Bernarda Vonneguta , aplikował na Uniwersytet Cornella , gdzie został przyjęty na chemię .
