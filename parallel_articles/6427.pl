Kurume
– miasto w Japonii , w prefekturze Fukuoka ( dawny region Chikugo ) , w północnej części wyspy Kiusiu ( `` Kyūshū '' ) , nad rzeką Chikugo .
Historia .
Kurume ma długą historię , ludzie zamieszkiwali te tereny około 20 tys .
