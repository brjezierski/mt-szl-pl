Kurza Góra
Kurza Góra – wieś w Polsce położona w województwie wielkopolskim , w powiecie kościańskim , w gminie Kościan , w pobliżu kanał Obra .
Część wsi należąca do starostwa kościańskiego , pod koniec XVI wieku leżała w powiecie kościańskim województwa poznańskiego .
We wczesnym średniowieczu na południowy zachód od Kurzej Góry istniał gród słowiański , którego pozostałościami są zachowane do dzisiaj fragmenty wałów .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) wzmiankowane są dwie miejscowości : Kurza góra stara i Kurza góra nowa .
Obie należały do wsi większych w ówczesnym powiecie Kosten rejencji poznańskiej .
Kurza góra stara i nowa należały do okręgu kościańskiego tego powiatu i stanowiły część majątku Bonikowo , który należał wówczas do Anzelma Chłapowskiego .
Według spisu urzędowego z 1837 roku Kurza góra stara liczyła 261 mieszkańców i 18 dymów ( domostw ) , natomiast Kurza góra nowa liczyła 115 mieszkańców i 15 dymów .
W latach 1975–1998 miejscowość administracyjnie należała do województwa leszczyńskiego .
