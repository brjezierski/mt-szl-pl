Kutno
Kutno – miasto w centralnej Polsce , w województwie łódzkim , siedziba powiatu kutnowskiego oraz wiejskiej gminy Kutno , nad rzeką Ochnią ( dopływem Bzury ) .
Powstanie miejscowości mogło nastąpić już w połowie XII wieku .
Prywatne miasto szlacheckie lokowane w 1386 roku .
W XII–XIII znajdowało się w kasztelanii łęczyckiej prowincji łęczyckiej i w kasztelanii łęczyckiej księstwa łęczyckiego .
W XVI wieku położone było w powiecie gostynińskim województwa rawskiego .
