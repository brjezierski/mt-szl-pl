Kuwejt
Kuwejt , Państwo Kuwejt ( دولة الكويت , trl .
Dawlat al-Kuwayt , trb .
Daulat al-Kuwajt ) – państwo położone w południowo-zachodniej Azji , na północno-zachodnim wybrzeżu Zatoki Perskiej .
Graniczy z Irakiem ( 242 km ) i Arabią Saudyjską ( 222 km ) – łączna długość granic lądowych wynosi 464 km , ponadto 499 km wybrzeża morskiego .
Historia .
Tereny obecnego Kuwejtu w VII wieku znalazły się w granicach kalifatu arabsko-muzułmańskiego .
Na początku XVI wieku na terenie obecnego Kuwejtu powstała portugalska faktoria handlowa .
W 1710 roku uciekinierzy z terenów dzisiejszej Arabii Saudyjskiej założyli miasto Kuwejt .
Od 1756 roku kraj znajdował pod władzą panującej do dziś dynastii as-Sabah a w 1899 roku stał się protektoratem brytyjskim .
Gospodarkę kraju zmienił fakt odkrycia w 1938 roku złóż ropy naftowej , już w 1945 roku Kuwejt stał się światowym potentatem w wydobyciu ropy .
Od początku 1961 roku zaczęło się przejmowanie władzy przez Kuwejtczyków z brytyjskich rąk i wprowadzanie przez nich prawa miejscowego .
19 czerwca 1961 roku Kuwejt uzyskał niepodległość po podpisaniu układu z Wielką Brytanią dającego Kuwejtowi gwarancje wojskowe .
W 1961 roku Kuwejt wstąpił do Ligi Państw Arabskich .
W 1963 roku Irak potwierdził wcześniejsze ustalenia z lat 1913 i 1932 o granicy między państwami .
W 1969 roku Kuwejt i Arabia Saudyjska podpisały układ o podziale między siebie Strefy Neutralnej nazywanej od tej pory Strefą Podzieloną .
2 sierpnia 1990 roku Irak dokonał inwazji na Kuwejt i opanował całe terytorium znacznie słabszego sąsiada w ciągu kilku dni .
W kraju jednocześnie miał miejsce proiracki zamach stanu , w wyniku którego powstała Republika Kuwejtu .
Od początku państwo to było traktowane przez Saddama Husajna jako byt przejściowy ; pod koniec sierpnia włączono je , jako 19. prowincję , w skład państwa irackiego .
W lutym 1991 roku siły ONZ pod dowództwem Stanów Zjednoczonych – po trwającej około 6 tygodni wojnie odbiły Kuwejt z rąk irackich i przekazały władzę dotychczasowemu monarsze .
Wycofujące się z Kuwejtu wojska irackie z rozmysłem niszczyły infrastrukturę kraju , w tym szyby naftowe .
