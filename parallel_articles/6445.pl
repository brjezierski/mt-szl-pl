Kvasiny
Kvasiny – wieś w Czechach , w kraju hradeckim , w powiecie Rychnov nad Kněžnou ; siedziba gminy ( cz .
`` obec '' ) wiejskiej Kvasiny .
Pierwsza wzmianka o niej pochodzi z 1544 .
Znajduje się tam fabryka Škody , w której produkowana jest Škoda Superb , a od 2009 r. także SUV Škoda Yeti .
Wcześniej produkowane były Škoda 110R , Škoda Rapid oraz Škoda Roomster .
Według danych z 28 sierpnia 2006 r. miejscowość zamieszkiwało 1376 osób .
