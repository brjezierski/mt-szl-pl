Kwas chlebowy
Kwas chlebowy ( pot .
kwas ) − napój klasyfikowany jako bezalkoholowy , mimo niewielkiej zawartości alkoholu , wytwarzany przez fermentację chleba .
Znany od średniowiecza , rozpowszechniony w krajach wschodniej i północnej Europy – przede wszystkim w Rosji , na Ukrainie , Białorusi i w krajach bałtyckich .
Napój przygotowuje się pospolicie w gospodarstwie domowym , a od końca XIX wieku także fabrycznie .
Właściwości .
Gęstość kwasu wynosi 1,006−1,016 g/cm³ , zawartość alkoholu 0,7−2,2 % objętościowych , substancji wyciągowych 1−5,2 % , kwasu mlekowego 0,18−0,48 % ; zawiera ponadto bardzo wiele drożdży , mało bakterii .
Kwas chlebowy ma naturalnie słodko-kwaśny zapach i smak pieczonego chleba , dobrze gasi pragnienie ( nie jest za słodki ) i długo utrzymuje niską temperaturę po schłodzeniu .
Ma jasno-brązowy kolor , na dnie butelki może utworzyć się naturalny osad , który znika po wstrząśnięciu butelką .
Wytwarzanie .
Jest otrzymywany przez alkoholową i kwaśną fermentację chleba z dodatkiem cukru lub owoców i drożdży górnych , przyprawiany zwykle miętą .
