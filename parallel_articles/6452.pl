Kwidzyn leży na obszarze dawnej Pomezanii , w historycznych Prusach Górnych , a także na Powiślu .
Według danych GUS z 30 czerwca 2021 r. , Kwidzyn liczył 38 179 mieszkańców i był pod względem liczby ludności dziewiątym miastem w województwie pomorskim .
Historia .
Pomezańskie grodzisko o nazwie `` Kwedis '' ( w zapisach kronikarskich wymieniana jako Quedin , Quidino , Quedzyn ) istniało w XI w. Najprawdopodobniej zostało zniszczone podczas walk toczonych na tych terenach .
Osada została ponownie założona przez zakon krzyżacki w 1233 przez mistrza Hermanna von Balka jako baza wypadowa w pruskiej Pomezanii .
Gród otrzymał prawa miejskie po lokacji na prawie chełmińskim w 1233 lub 1235 ( potwierdzone ok. 1254 ) , posiadało układ typowo szachownicowy i składało się z ok. 50 parceli .
Miasto zostało zniszczone podczas walk prusko-krzyżackich w 1243 .
