Kylian Mbappé
Kylian Mbappé Lottin ( wym .
[ ] ; ur .
20 grudnia 1998 w Paryżu ) – francuski piłkarz kameruńsko-algierskiego pochodzenia , występujący na pozycji napastnika we francuskim klubie Paris Saint-Germain oraz w reprezentacji Francji , której jest kapitanem .
Uważany przez wielu za jednego z najlepszych piłkarzy swojego pokolenia na świecie .
Karierę seniorską rozpoczął od występów dla AS Monaco , z którym w sezonie 2016/2017 zdobył mistrzostwo Francji .
W 2017 został wypożyczony do Paris Saint-Germain , a rok później paryski klub wykupił go za 180 milionów euro .
Czterokrotnie wygrywał Ligue 1 i cztery razy kończył ligowe rozgrywki jako król strzelców .
Jest najskuteczniejszym zawodnikiem w historii Paris Saint-Germain , a także najskuteczniejszym zawodnikiem tego klubu w rozgrywkach UEFA .
W 2020 dotarł z drużyną do finału Ligi Mistrzów UEFA .
W reprezentacji Francji występuje od 2017 , od 2023 jest kapitanem drużyny narodowej .
Został złotym medalistą Mistrzostw Świata 2018 , nagrodzono go również nagrodą dla Najlepszego Młodego Zawodnika .
Na Mistrzostwach Świata 2022 został finalistą turnieju , a z 8 golami został królem strzelców .
Uczestnik Mistrzostw Europy 2020 .
Dwukrotnie wybierany był Piłkarzem Roku we Francji .
W 2018 został laureatem nagrody Kopa Trophy , przyznawanej przez „ France Football ” .
W latach 2018 , 2021 i 2022 był uwzględniany w Drużynie roku przez Międzynarodową Federację Historyków i Statystyków Futbolu .
W 2019 , wraz z drużyną narodową , został odznaczony Legią Honorową .
Młodość i pochodzenie .
Urodził się w Paryżu jako syn Wifreda Mbappé i Fayzy Lamari .
Jego ojciec jest z pochodzenia Kameruńczykiem i jest agentem piłkarskim , a matka jest pochodzenia algierskiego , wywodzi się z plemienia Kabylów i była reprezentantką Francji w piłce ręcznej .
