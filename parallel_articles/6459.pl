Kymco
Kwang Yang Motor Company , w skrócie Kymco – tajwański największy producent skuterów , motocykli , i quadów , powstały w 1963 roku w wyniku kooperacji z Honda Corporation .
Historia .
Przez pierwsze 30 lat istnienia Kymco zajmowało się produkcją motocykli i skuterów na licencji Hondy oraz dostarczaniem części motocyklowych , silnikowych oraz samochodowych dla japońskich fabryk Hondy .
Od 1993 wszystkie motocykle są produkowane samodzielnie pod marką Kymco , ale technologie ściśle bazują na tych , tworzonych przez Hondę .
W 1998 roku akumulowana produkcja wytwórni osiągnęła poziom 6 milionów sztuk .
W 2000 roku KYMCO było największym producentem skuterów na Tajwanie i 5 największym na świecie .
W 2008 KYMCO zaczęło produkować silniki dla BMW G450X Enduro bike .
W 2013 wspólnie z Kawasaki zaczęli robić skuter J300 , bazując na 300i od KYMCO .
