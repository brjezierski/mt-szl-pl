Kępno ( województwo pomorskie )
Kępno ( kaszb .
`` Kãpnò '' , .niem .
`` Kempen '' ) – wieś w Polsce położona w województwie pomorskim , w powiecie słupskim , w gminie Słupsk .
Wieś jest częścią składową sołectwa `` Wrzeście '' .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
