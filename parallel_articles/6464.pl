Kędzierzynka
Kędzierzynka – wieś w Polsce położona w województwie małopolskim , w powiecie myślenickim , w gminie Dobczyce .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krakowskiego .
Przysiółki wchodzące w skład sołectwa .
Knieje , Brusznica , Boczoniec ( Boconiec ) , Zadziele , Pod Willą , Kobylanka ( Kobylonka ) , Siciny , Brzegi , Pod Olszyną , Łącki , Zielona , Potoki , Działy ( Dzioły ) , Sękówka , Granice , Brzeg , Koźmicówka .
Sołectwo zajmuje obszar 498,3 ha .
