Kętrzyn ( stacja kolejowa )
Kętrzyn – stacja kolejowa w Kętrzynie , w województwie warmińsko-mazurskim , w Polsce .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
Powstała w 1867 roku , po otwarciu połączenia kolejowego do Bartoszyc .
Rok później powstał szlak do Ełku , zaś w 1907 roku otwarto połączenie do Węgorzewa .
Stacja posiada 5 torów głównych , z których jeden posiada możliwość wyjazdu jedynie w kierunku Węgorzewa .
Znajduje się tutaj dosyć dużo torów bocznych , przy których znajdują się place ładunkowe , magazyny , rampy .
Ruchem na stacji steruje się z dwóch nastawni : `` Kn '' od strony Giżycka i `` Kn1 '' od strony Korsz .
Wyposażone są one w urządzenia mechaniczne scentralizowane z sygnalizacją kształtową .
Jedynie semafor wjazdowy od strony Giżycka i wszystkie tarcze ostrzegawcze zostały wymienione na świetlne .
Nadal jednak przy dwóch semaforach wjazdowych znajdują się kształtowe tarcze przelotowe .
Od czerwca 2014 roku zmodernizowano budynek dworca PKP , parkingu oraz całej ulicy Dworcowej .
W budynku dworca : kasa PKP Intercity , galeria , toalety , biuro spółki Komunalna Energetyka Cieplna , Młodzieżowe Centrum Kariery OHP , Polskie Towarzystwo Turystyczno-Krajoznawcze .
Od 2016 roku uruchamiane są sezonowe połączenia Kętrzyn - Węgorzewo , organizowane przez SKPL Cargo
