Kęty
Kęty – miasto w województwie małopolskim , w powiecie oświęcimskim , siedziba gminy miejsko-wiejskiej Kęty .
Kęty to miasto o charakterze handlowo-przemysłowym , położone na Podgórzu Wilamowickim ( na północ od Beskidu Małego ) , w dolinie rzeki Soły .
Według danych GUS z 30 czerwca 2021 r. miasto liczyło 18 372 mieszkańców .
Układ urbanistyczny Kęt został wpisany do rejestru zabytków województwa małopolskiego .
Kęty uzyskały lokację miejską przed 1267 rokiem .
Miasto królewskie starostwa oświęcimskiego w powiecie śląskim województwa krakowskiego w końcu XVI wieku .
Położenie geograficzne .
Kęty położone są w Kotlinie Oświęcimskiej , w dolinie rzeki Soły , na jej stożku napływowym .
