Język kirgiski
Język kirgiski ( , قىرعىزچا , قىرعىز تىلى ) – język należący do rodziny języków tureckich .
Jest językiem urzędowym Kirgistanu , obok rosyjskiego .
Porozumiewa się nim ok. 5 mln ludzi , głównie Kirgizów w swoim kraju , ale też w Afganistanie , Chinach , Kazachstanie , Tadżykistanie , Turcji i Uzbekistanie .
Za alfabet służy językowi kirgiskiemu cyrylica rozbudowana o trzy dodatkowe litery : Ңң , Үү , Өө .
Język kirgiski był zapisywany alfabetem łacińskim między 1928 a 1940 r. Od odzyskania niepodległości w 1991 r. wśród niektórych kirgiskich polityków istniał pomysł , by przywrócić język kirgiski do zapisywania łacinką , ale do tej pory nie wszedł on w życie .
