Kyzył
Kyzył ( ros .
Кызы́л ) – miasto w Federacji Rosyjskiej , stolica Republiki Tuwy .
Miejski kod OKATO to 93 401 .
Informacje ogólne .
W Kyzyle mieszka około 110 tysięcy ludzi ( w tym około 50 % Rosjan ) , co stanowi jedną trzecią ludności republiki .
Na Kyzył przypada 70 % produkcji przemysłowej Tuwy .
Skupia się tu właściwie całe życie naukowe i kulturalne republiki .
Ważniejszymi instytucjami są Tuwiński Uniwersytet Państwowy , republikańskie muzeum krajoznawcze Ałdan-Maadyr , teatr Kok-ooła .
Miasto utrzymuje bezpośrednie połączenia lotnicze z Krasnojarskiem , Abakanem , Nowosybirskiem .
Geografia .
Miasto jest położone ok. 4700 km na wschód od Moskwy i ok. 301 km na wschód od Ak-Dowurak – innego ważnego ośrodka miejskiego w Tuwie .
Kyzył znajduje się w geograficznym centrum Azji i łączą się tu , tworząc główny nurt Jeniseju ( po tuwińsku `` Uług-Chem '' ) , dwa jego dopływy : Wielki Jenisej ( tuw .
`` Bij-Chem '' ) i Mały Jenisej ( tuw .
`` Kaa-Chem '' ) .
Niedaleko miejsca połączenia obu rzek nad brzegiem stoi obelisk wskazujący środek Azji , z napisami po : tuwińsku , rosyjsku i angielsku .
Historia .
Kyzył założony został w 1914 r. pod nazwą `` Biełocarsk '' , w 1918 przemianowano go na Chem-Bełdyr .
Dzisiejszą nazwę – oznaczającą po tuwińsku `` czerwony '' – nadano miastu w 1926 r .
We wrześniu 2004 r. Kyzył obchodził 90. rocznicę przyznania praw miejskich oraz 60. rocznicę przyłączenia Tuwy do ZSRR .
