Kenia ( komiks )
Kenia ( tytuł oryg .
`` Kenya '' ) – francuska seria komiksowa z gatunku science fiction , autorstwa Luiza Eduarda De Oliveiry , tworzącego pod pseudonimem Leo ( scenariusz i rysunki ) , i Rodolphe ' a Daniela Jacquette ' a , tworzącego pod pseudonimem Rodolphe ( scenariusz i dialogi ) .
Została opublikowana w pięciu tomach w latach 2001–2009 przez wydawnictwo Dargaud .
W Polsce seria ukazała się nakładem wydawnictwa Egmont Polska w jednym albumie zbiorczym w 2018 .
Kontynuacjami `` Kenii '' są serie komiksowe `` Namibia '' ( 5 tomów , 2010–2015 ) i `` Amazonie '' ( 3 tomy , od 2016 ) .
Opis fabuły .
W 1947 roku Kenii dochodzi do tajemniczych wydarzeń : pojawiają się niezidentyfikowane obiekty latające , nieznane gigantyczne zwierzęta , a w czasie safari znika grupa turystów .
Kathy Austin , nowa lokalna nauczycielka historii , a w rzeczywistości agentka MI6 , organizuje wyprawę do dżungli , by rozwikłać zagadkę zaginięcia .
W zespole poszukiwawczym są jednak szpiedzy innych mocarstw .
