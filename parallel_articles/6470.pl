Kálmán Ihász
Kálmán Ihász ( ur .
6 marca 1941 w Budapeszcie , zm .
31 stycznia 2019 ) – węgierski piłkarz grający na pozycji obrońcy .
Brązowy medalista ME 64 oraz mistrz olimpijski z Tokio .
Profesjonalnie grał tylko w budapeszteńskim Vasasie ( 1958–1973 ) , wcześniej był zawodnikiem młodzieżowych zespołów tego klubu .
Z Vasasem był czterokrotnie mistrzem kraju ( 1961 , 1962 , 1965 , 1966 ) .
W reprezentacji Węgier debiutował w 1962 , ostatni raz zagrał w 1969 .
Łącznie rozegrał 27 oficjalnych spotkań .
Znajdował się w kadrze na dwa turnieje finałowe mistrzostw świata : MŚ 62 i MŚ 66 .
W 1964 z olimpijską reprezentacją Węgier triumfował w igrzyskach .
