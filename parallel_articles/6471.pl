Kóny
Kóny – wieś i gmina w północno-zachodniej części Węgier , w pobliżu miasta Csorna .
Gmina liczy 2 631 mieszkańców ( styczeń 2011 ) i zajmuje obszar 28,88 km² .
Miejscowość leży na obszarze Małej Niziny Węgierskiej , kilkanaście kilometrów od granic słowackiej i austriackiej .
Administracyjnie należy do powiatu Csorna , wchodzącego w skład komitatu Győr-Moson-Sopron .
