Kiejdany ( Białoruś )
Kiejdany – dawny folwark .
Tereny na których leżał znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie dokszyckim , w sielsowiecie Krypule .
Historia .
W czasach zaborów folwark leżał w gminie Pariafianów , w powiecie wilejskim w guberni wileńskiej Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie wileńskim , w powiecie duniłowickim , od 1926 w powiecie dziśnieńskim , w gminie Parafianów .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 13 osób , 6 było wyznania rzymskokatolickiego a 7 prawosławnego .
Jednocześnie wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Były tu 2 budynki mieszkalne .
W 1931 w 2 domach zamieszkiwało 13 osób .
Wierni należeli do parafii rzymskokatolickiej w Parafianowie i prawosławnej w Dokszycach .
Miejscowość podlegała pod Sąd Grodzki w Dokszycach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Parafianowie .
