Kłobuczyno
Kłobuczyno ( dodatkowa nazwa w j. kaszub .
Kłobùczëno ; niem .
`` Klobschin '' ) – wieś kaszubska w Polsce położona w województwie pomorskim , w powiecie kościerskim , w gminie Kościerzyna , przy drodze krajowej nr 20 Stargard – Szczecinek – Gdynia , na terenie Pojezierza Kaszubskiego u podnóży Wzgórz Szymbarskich i ich najwyższej kulminacji Wieżycy ( 328 m n.p.m . ) .
Jest to najdalej na północ wysunięty zakątek gminy Kościerzyna , a zarazem najwyżej położona wieś powiatu kościerskiego ( 252 m n.p.m .
) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Charakterystyka miejscowości .
Kłobuczyno jest jednym z 35 sołectw gminy , zajmuje obszar o powierzchni 1395,49 ha , a w jego skład wchodzi jedynie miejscowość Kłobuczyno .
Ponad połowę powierzchni sołectwa stanowią użytki rolne , a ponad 1/3 stanowią lasy .
Znajduje się tu punkt przedszkolny , które kiedyś było szkołą podstawową oraz zakład przerobu betonu i materiałów budowlanych , który wybudowano w 1965 roku na bazie byłej Rolniczej Spółdzielni Produkcyjnej .
Krajobraz sołectwa tworzą pagórkowate połacie z obszarami leśnymi .
