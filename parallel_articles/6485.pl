Kłoda ( narzędzie kary )
Kłoda - początkowo pień drzewa rozpiłowany wzdłuż , później dwie belki , z wycięciami na ręce lub nogi , służący do unieruchamiania złoczyńców lub jeńców .
Pierwsze wzmianki o `` kłodnikach '' pochodzą z r. 1261 ( według Glogera ) , w `` Kazaniach Świętokrzyskich '' ( pocz .
XIV w. ) ( według Brücknera ) .
Ksiądz Piotr Skarga w `` Żywotach świętych '' pisze : „ Wsadzono ich do kłody ” , „ Wieść go do ciemnicy i mocno mu w kłodzie nogi cisnąć kazał ” .
Użycie kłody opisał Adam Mickiewicz w `` Panu Tadeuszu '' ( księga IX , `` Bitwa '' ) .
