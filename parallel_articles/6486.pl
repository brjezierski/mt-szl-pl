Kłodawa ( dopływ Motławy )
Kłodawa ( niem .
`` Kladau ) '' – rzeka , dopływ Motławy o długości 33,85 km .
Rzeka płynie na Pobrzeżu Gdańskim .
Wypływa z Jeziora Małego Mierzeszyńskiego na wschodnim skraju Pojezierza Kaszubskiego .
Górny bieg rzeki to liczne zakola i zalesione jary o stosunkowo wysokim spadzie .
Na zachód od drogi Gdańsk – Starogard Gdański znajduje się ustanowiony w 1999 rezerwat przyrody Dolina Kłodawy , chroniący unikatową roślinność leśną przełomowego odcinka rzeki ( na odcinku 1,6 km ) oraz fragmentów wysoczyznowego lasu grądowego .
W dolnym odcinku rzeka ( od Łęgowa ) przechodzi w nizinny kanał rzeczny aż do ujścia do Motławy w miejscowości Grabiny-Zameczek .
