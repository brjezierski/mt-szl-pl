Festiwal Interwizji
Interwizja , Festiwal Interwizji ( ros .
Интервидение ) , także : Konkurs Piosenki Interwizji – festiwal muzyczny organizowany w latach 1977–1980 w Operze Leśnej w Sopocie , odpowiednik Konkursu Piosenki Eurowizji w bloku wschodnim .
Wcześniej , pod tą samą nazwą , organizowany był konkurs w Czechosłowacji w latach 1965–1968 .
