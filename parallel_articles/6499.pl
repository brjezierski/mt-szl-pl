Starsze konsole miały natomiast od jednej do kilkudziesięciu gier zapisanych na stałe w nieusuwalnej pamięci tylko do odczytu , co było bardzo wygodne , lecz zdecydowanie ograniczało możliwość wyboru gier .
Historia konsoli .
Historię konsol dzieli się na generacje , gdzie każda z nich rozpoczyna się wydaniem nowych urządzeń .
Pierwszą konsolą była Magnavox Odyssey , która została zaprojektowana przez Ralpha Baera i wydana w 1972 .
Podczas drugiej generacji wydano takie konsole jak Fairchild Channel F czy Atari 2600 .
W 1983 popularność komputerów osobistych doprowadziła do zapaści skutkującej mniejszą liczbą sprzedanych konsol .
Wraz z czwartą generacją pojawiły się 16-bitowe konsole Sega Mega Drive i Super Nintendo Entertainment System .
W trakcie piątej generacji bardzo popularne stały się gry trójwymiarowe `` Super Mario 64 '' na Nintendo 64 i `` Tomb Raider '' na Sega Saturn .
Początek szóstej generacji datuje się na 1998 , kiedy to wydano Dreamcasta , pierwszą konsolę z możliwością grania online .
