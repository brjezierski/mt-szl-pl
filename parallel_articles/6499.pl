Konsola gier wideo
Konsola gier wideo – komputer o wyspecjalizowanej architekturze przeznaczony głównie do uruchamiania gier .
Opis .
W przeciwieństwie do komputerów osobistych konsole mają ograniczony interfejs użytkownika i dostęp do ustawień administracyjnych .
Zazwyczaj nie jest również możliwe unowocześnianie podzespołów .
W niektórych konsolach możliwe jest zainstalowanie w pełni funkcjonalnego systemu operacyjnego .
Komputer zwykle jest umieszczony w niedużej obudowie wyposażonej w niezbędne złącza ( dla gamepadów , telewizora , zasilania i innych ) .
