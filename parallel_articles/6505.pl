Księga Powtórzonego Prawa
Księga Powtórzonego Prawa [ Pwt ] ( hebr .
דברים `` Dwarim '' , słowa ; gr .
`` Δευτερονόμιον '' , Powtórzone Prawo ; łac .
`` Deuteronomium '' ) – ostatnia , piąta księga `` Tory '' .
Zawiera między innymi przepisy prawa , które Bóg podyktował Mojżeszowi na górze Horeb ( tzw .
kodeks deuteronomiczny ) , poprzedzone Dekalogiem .
W księdze tej umieszczony jest również hymn Mojżesza .
`` Księga Powtórzonego Prawa '' kończy się śmiercią Mojżesza i wkroczeniem Izraelitów do Ziemi Obiecanej .
Dalsze wydarzenia opisuje `` Księga Jozuego '' .
Przepisy prawne z `` Księgi Powtórzonego Prawa '' uważane są za najstarszą część `` Pięcioksięgu '' .
Historia kompozycji .
W 1806 roku Wilhelm Martin Leberecht de Wette zauważył , że przepisy prawne i kultowe z rozdziałów 12-26 Księgi Powtórzonego Prawa odpowiadają reformie króla Jozjasza , datowanej na 622 rok p.n.e .
i opisanej w rozdziale 23 `` 2 Księgi Królewskiej '' .
De Wette uznał , że księga powstała dla uzasadnienia centralizacji państwa i ustanowienia wyłącznego kultu jerozolimskiego boga Jahwe .
W XX wieku zauważono , że tekst przymierza , jakie Bóg zawarł z Mojżeszem , wzorowany był na ówczesnych traktatach asyryjskich .
Księga była później rozbudowywana o kolejne elementy - dodano do niej między innymi Dekalog .
Jej bezpośrednią kontynuację stanowi `` Księga Jozuego '' .
Księgi od `` Powtórzonego Prawa '' do `` Drugiej Księgi Królewskiej '' tworzą tak zwaną `` historię deuteronomistyczną '' , napisaną przez autorów o zbliżonym stylu i światopoglądzie .
Przedmiotem kontrowersji pozostaje , czy historia deuteronomistyczna powstawała niezależnie od pozostałych ksiąg `` Pięcioksięgu '' ( zwanych też `` Tetrateuchem '' , a więc `` Czteroksięgiem '' ) aż do momentu ostatecznej redakcji `` Tory '' ( jak chciał Martin Noth ) .
Teologia deuteronomiczna .
Teologia deuteronomiczna ukształtowała się prawdopodobnie pod wpływem proroków z królestwa północnego .
W centrum `` Księgi Powtórzonego Prawa '' stoi przymierze , jakie Bóg zawarł z Mojżeszem na górze Horeb ( przez późniejszych autorów `` Pięcioksięgu '' zwanej górą Synaj ) .
Przymierze ma charakter warunkowy : Bóg będzie stał po stronie Żydów tak długo , jak będą przestrzegać jego prawa i bilateralny .
W rezultacie pierwsi prorocy okresu wygnaniowego tłumaczyli niewolę babilońską właśnie złamaniem przymierza .
