Księstwo prudnickie
Księstwo prudnickie ( , , ) – dawne księstwo śląskie istniejące w latach 1318–1424 ze stolicą w Prudniku .
Historia .
Księstwo zostało wydzielone z Królestwa Czech przez Mikołaja II opawskiego z dynastii Przemyślidów w 1318 roku .
Mikołaj panował nad księstwem do 1337 , kiedy to bezpotomnie zmarł jego szwagier książę raciborski Leszek .
Król Czech Jan Luksemburski zdecydował się wtedy przekazać opróżnione lenno właśnie księciu opawskiemu .
