Księżopole-Jałmużny
Księżopole-Jałmużny ( do 2010 `` Książopole-Jałmużny '' ) – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Jadwigi w Mokobodach .
Zaścianek szlachecki Jałmużny należący do okolicy zaściankowej Księżopole położony był w drugiej połowie XVII wieku w ziemi drohickiej województwa podlaskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
