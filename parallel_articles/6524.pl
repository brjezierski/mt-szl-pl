Konfucjusz
Konfucjusz ( ; ur .
551 p.n.e .
w Qufu , zm .
479 p.n.e .
tamże ) – chiński filozof , twórca konfucjanizmu .
Konfucjusz nie pozostawił pism – jego nauczanie zebrano w księdze `` Dialogi konfucjańskie '' .
Głównym tematem rozważań było codzienne życie ludzi i stabilność społeczeństwa .
Jego zdaniem podstawowymi cnotami są : humanitaryzm , praworządność , poprawność , mądrość i lojalność .
Cnoty te realizują się w pięciu powinnościach , czy też fundamentalnych relacjach społecznych : między panującym i urzędnikiem , ojcem i synem , starszym i młodszym bratem , mężem i żoną oraz między przyjaciółmi .
Moralność jednostki i podstawowa rola rodziny jest fundamentem i gwarantem stabilności państwa .
Konfucjusz dużą rolę w kształtowaniu ładu społecznego przypisywał wykształceniu , które obejmowało także kwestie ducha i serca .
Doktryna Konfucjusza ( konfucjanizm ) była rozwijana początkowo przez uczniów , a później przez filozofów-następców i komentatorów ( Mencjusz , Xunzi , neokonfucjanizm ) .
Myśl konfucjańska objęła zasięgiem obszar Chin , Japonii , Korei i Wietnamu .
Pochodzenie .
Ród , z którego wywodził się Konfucjusz był według tradycji spauperyzowaną gałęzią rodu dawnych władców Shang .
Jego ojciec , Shuliang He ( 叔梁紇 ) , nosił jednak pospolite nazwisko Kong i służył jako żołnierz oraz urzędnik wojskowy .
Konfucjusz urodził się jako dziecko z drugiego małżeństwa , zawartego przez Shulianga He w wieku ponad pięćdziesięciu lat w celu spłodzenia zdrowego , męskiego potomka ( z pierwszego małżeństwa miał wyłącznie dziewięć córek i jednego kalekiego syna ) .
Ojciec Konfucjusza umarł kiedy syn miał trzy lata .
Kontrowersyjny charakter jego związku skazał matkę przyszłego filozofa oraz samego Konfucjusza na życie w odcięciu od rodziny .
Życie .
Biografia Konfucjusza zawarta jest w 77. rozdziale „ Shiji ” ( Zapiski Historyczne ) .
Na imię miał Qiu ( 丘 ) , czyli „ pagórek ” , prawdopodobnie z powodu wypukłego czoła , według wierzeń Chińczyków , znamionującego inteligencję .
Zwany był też Zhongni ( 仲尼 ) .
Urodził się w państwie Lu ( obecnie prowincja Shandong ) .
Wychowywała go samotnie matka , poznał biedę i trudy życia .
Dzieciństwo spędził w Qufu pod Yanzhou , gdzie od małego musiał fizyczną pracą zarabiać na siebie i swoją matkę .
Mając 15 lat podjął naukę , którą sam opłacał wytężoną pracą .
Posiadając początkowo jedynie umiejętność strzelania z łuku , był nadzorcą stad owiec i wołów .
W wieku 19 lat ożenił się i rozpoczął karierę urzędniczą .
Zdobył sławę człowieka niezwykle uczonego , miał znać na pamięć klasyczną „ Księgę Pieśni ” .
Był kolejno naczelnikiem spichlerzy , nadzorcą pól książęcych i ludzi doglądających zwierząt ofiarnych .
W 530 p.n.e .
założył szkołę , w której uczono pisma , zasad zachowania się i podstawowej wiedzy .
Około 517 p.n.e .
spotkał się z Laozi .
