Konklawe
Konklawe ( łac .
`` conclave '' , zamknięte pomieszczenie w budynku , pokój ) – ogólne zgromadzenie kardynałów dla wyboru papieża .
Odbywa się w kaplicy Sykstyńskiej .
Konklawe poprzedzone jest okresem `` Sede vacante '' .
Zwoływane jest po 15 , maksymalnie po 20 dniach od śmierci lub rezygnacji papieża .
Okres poprzedzający rozpoczęcie konklawe po śmierci papieża reguluje konstytucja Universi Dominici gregis .
Jednak w przypadku rezygnacji , papież może wydać dekret , w którym skróci obowiązek zwołania konklawe po upływie minimum 15 dni .
Historia .
W ciągu wieków różnie wybierano biskupów także biskupa Rzymu .
Na pewno do czasów Konstantyna Wielkiego , czyli do przełomu III i IV wieku biskupi , a także papież , wybierani byli przez członków gminy chrześcijańskiej .
Lud rzymski obwoływał nowego papieża a duchowieństwo jego osobę akceptowało .
Chcąc uniknąć wpływów świeckich na elekcje papieskie Feliks IV wyznaczył na swojego następcę Bonifacego II .
Ze względu na sprzeciw cesarza Justyniana senat wydał edykt zakazujący jakiejkolwiek dyskusji nt .
sukcesji papieskiej , za życia papieża .
Stefan IV w 769 r. zastrzegł wybór nowego papieża dla duchowieństwa rzymskiego .
Mimo to w 1073 r. podczas uroczystości pogrzebowych Aleksandra II w bazylice św .
Jana na Lateranie lud rzymski wznosił okrzyki „ Hildebrand papieżem ” .
Wolę tę uwzględnili kardynałowie wybierając przez aklamację Grzegorza VII .
Zasadę wyboru papieża przez kardynałów wprowadził Mikołaj II podczas synodu w roku 1059 .
Jednakże nie byli oni odcięci od zewnętrznych nacisków , co więcej wybór musiał zostać dokonany w obecności posła cesarskiego i przez cesarza zatwierdzony .
