Kąkolewnica
Kąkolewnica – wieś gminna we wschodniej Polsce położona w województwie lubelskim , w powiecie radzyńskim , w gminie Kąkolewnica ( do 2010 pn .
`` gmina Kąkolewnica Wschodnia '' ) , której jest siedzibą .
Do końca 2010 roku były to cztery wsie : Kąkolewnica Wschodnia , Kąkolewnica Południowa , Kąkolewnica Północna i Rudnik , z których Kąkolewnica Wschodnia stanowiła siedzibę gminy .
1 stycznia 2011 wszystkie cztery wsie połączono w jedną o nazwie Kąkolewnica .
Wieś królewska w starostwie kąkolewnickim w ziemi łukowskiej województwa lubelskiego w 1786 roku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
17 sierpnia 1920 roku , podczas Bitwy Warszawskiej , polska Brygada Jazdy Ochotniczej w boju pod Kąkolewnicą pokonała bolszewicką 170 Brygadę Strzelców .
W listopadzie 1943 r. doszło tu do walki 6 batalionu Gwardii Ludowej z oddziałem Narodowych Sił Zbrojnych .
