Konkurs Piosenki Eurowizji 2008
53 .
Konkurs Piosenki Eurowizji został rozegrany 20 , 22 i 24 maja 2008 i zorganizowany w Belgradzkiej Arenie przez serbskiego nadawcę Radio-Televizija Srbija ( RTS ) .
Koncerty konkursowe poprowadzili Jovana Janković i Željko Joksimović .
Finał konkursu wygrał Dima Biłan , reprezentant Rosji z piosenką „ Believe ” , współtworzoną z Jimem Beanzem , za którą otrzymał łącznie 272 punkty .
