Dzięki wygranej Mariji Šerifović , reprezentantki Serbii podczas 52 .
Konkursu Piosenki Eurowizji , organizacja konkursu w 2008 przypadła serbskiemu nadawcy publicznemu Radio-Televizija Srbije ( RTS ) .
Grupa Referencyjna Europejskiej Unii Nadawców zdecydowała , że koncert odbędzie się w Belgradzie .
Konkurs odbył się w Belgradzkiej Arenie , trzeciej co do wielkości hali widowiskowo-sportowej w Europie , która średnio może pomieścić niemal 18,4 tys .
