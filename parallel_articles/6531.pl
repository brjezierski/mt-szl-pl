Konstantynów ( rejon grodzieński )
Konstantynów – dawny folwark położony w miejscu leżącym obecnie na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Skidel .
Historia .
Dawniej folwark dóbr Skidel w województwie trockim Rzeczypospolitej Obojga Narodów .
W czasach zaborów w granicach Imperium Rosyjskiego , w guberni grodzieńskiej .
W latach 1921–1939 folwark leżał w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Skidel .
Miejscowość należała do parafii prawosławnej w Skidlu i rzymskokatolickiej w Kaszubińcach .
Podlegała pod Sąd Grodzki w Skidlu i Okręgowy w Grodnie ; właściwy urząd pocztowy mieścił się w Skidlu .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
4 grudnia 1939 włączona do nowo utworzonego obwodu białostockiego .
Od czerwca 1941 roku pod okupacją niemiecką .
22 lipca 1941 r. włączona w skład okręgu białostockiego III Rzeszy .
W 1944 miejscowość została ponownie zajęta przez wojska sowieckie i włączona do obwodu grodzieńskiego Białoruskiej SRR .
Od 1991 wchodzi w skład Białorusi .
