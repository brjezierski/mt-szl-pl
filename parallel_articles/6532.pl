Konstancin-Jeziorna
Konstancin-Jeziorna – miasto w woj .
mazowieckim , w powiecie piaseczyńskim , położone w aglomeracji warszawskiej ok. 20 km na południe od centrum Warszawy ; siedziba gminy miejsko-wiejskiej Konstancin-Jeziorna .
Powstało w 1969 r. w wyniku połączenia miast Skolimów-Konstancin i Jeziorna oraz kilku wsi .
Jest siedzibą władz gminy .
Burmistrzem miasta i gminy jest od grudnia 2010 roku Kazimierz Jańczuk .
Według danych stołecznego Urzędu Statystycznego z 31 grudnia 2021 , miasto miało 17 032 mieszkańców .
Znane jako uzdrowisko , dawniej także ze względu na produkcję papieru .
W normatywnej polszczyźnie nazwa miasta powinna przyjmować rzeczownikowy paradygmat odmiany , nie przymiotnikowy ( np .
`` do Konstancina-Jeziorny '' , `` w Konstancinie-Jeziornie '' , natomiast wszelkie formy zakończone na `` -Jeziornej '' nie mają aprobaty preskryptywistów ) .
Położenie .
Miejscowość położona jest na styku Równiny Warszawskiej i Doliny Środkowej Wisły , nad rzeką Jeziorką oraz jej dopływem , Małą .
