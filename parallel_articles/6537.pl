Kórnik
Kórnik ( niem .
`` Kurnik '' lub `` Kurnick '' , w latach 1939–1945 `` Burgstadt '' ) – miasto w województwie wielkopolskim , w powiecie poznańskim , nad Jeziorem Kórnickim , siedziba gminy miejsko-wiejskiej Kórnik .
Prywatne miasto szlacheckie lokowane w 1458 , do XIX w. nazywane zwykle Kurnikiem ( w tekstach łacińskich jako Curnik ) , alternatywne wersje pisowni ( Cornik , Kurnyk , Curnyk , Curniki , Kornik , Kórnik ) pojawiały się od XIV wieku .
Według XVI-wiecznego podziału administracyjnego , położone było w województwie kaliskim .
W latach 1975–1998 miasto administracyjnie należało do woj .
poznańskiego .
