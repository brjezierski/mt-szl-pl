Ludność .
Według danych z 31 grudnia 2010 roku miasto liczyło 7206 mieszkańców ( razem z Bninem ) .
W 1923 na Prowencie ( który obecnie jest częścią Kórnika ) urodziła się polska poetka , laureatka Nagrody Nobla z 1996 roku – Wisława Szymborska .
Była córką Wincentego Szymborskiego , zarządcy dóbr hrabiego Zamoyskiego oraz współtwórcy Rzeczypospolitej Zakopiańskiej .
