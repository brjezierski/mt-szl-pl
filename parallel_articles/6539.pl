Koniecpol ( Białoruś )
Koniecpol – dawny zaścianek .
Tereny na których leżał znajdują się obecnie na Białorusi , w obwodzie mińskim , w rejonie miadzielskim , w sielsowiecie Miadzioł .
Dawniej używana nazwa – Koncypole .
Historia .
W czasach zaborów w granicach Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym zaścianek leżał w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Mańkowicze ( od 1927 gmina Hruzdowo ) .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 2 osoby , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1931 w 1 domu zamieszkiwało 4 osoby .
Miejscowość należała do parafii rzymskokatolickiej w Hruzdowie .
Podlegała pod Sąd Grodzki w Postawach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Hruzdowie .
