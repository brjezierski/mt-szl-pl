Końskowola ( przystanek kolejowy )
Końskowola ( do 2020 : Pożóg ) – przystanek kolejowy w Końskowoli , w województwie lubelskim , w Polsce .
Znajdują się tu 2 perony .
W roku 2017 przystanek obsługiwał 0–9 pasażerów na dobę .
Historia .
Przystanek powstał w czasach carskich , wówczas we wsi Pożóg .
Później w wyniku zmian administracyjnych znalazł się w granicach Końskowoli , ale nazwę utrzymano .
Podczas remontu linii kolejowej w latach 2017-2019 zbudowano nowy przystanek z nowymi tablicami , po czym rada gminy Końskowola wystąpiła z wnioskiem o zmianę nazwy przystanku .
Spółka PKP-PLK zmieniła jego nazwę , przy czym kosztem w wysokości 23 tysiące złotych obciążyła wnioskodawcę .
