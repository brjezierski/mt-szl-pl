Koniuchy ( sielsowiet Skidel )
Koniuchy ( biał .
Канюхі ; ros .
Конюхи ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Skidel .
Leży nad rzeką Kotrą .
Koniuchy należały do ekonomii grodzieńskiej .
Odpadły od Rzeczypospolitej w wyniku III rozbioru .
Znalazły się w utworzonym powiecie grodzieńskim , guberni grodzieńskiej Imperium Rosyjskiego w gminie Jeziory .
W latach 1921–1939 wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Jeziory .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwały tu 204 osoby , 5 było wyznania rzymskokatolickiego a 199 prawosławnego , wszyscy mieszkańcy deklarowali polską przynależność narodową .
Było tu 46 budynków mieszkalnych .
Miejscowość należała do parafii prawosławnej w Hołowaczach i rzymskokatolickiej w Kaszubińcach .
Podlegała pod Sąd Grodzki w Skidlu i Okręgowy w Grodnie ; właściwy urząd pocztowy mieścił się w Skidlu .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 miejscowość znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
4 grudnia 1939 włączona do nowo utworzonego obwodu białostockiego .
Od czerwca 1941 roku pod okupacją niemiecką .
22 lipca 1941 r. włączona w skład okręgu białostockiego III Rzeszy .
W 1944 miejscowość została ponownie zajęta przez wojska sowieckie i włączona do obwodu grodzieńskiego Białoruskiej SRR .
Od 1991 r. w składzie niepodległej Białorusi .
