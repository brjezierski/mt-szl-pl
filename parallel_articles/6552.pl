LOL ( album GFriend )
LOL – pierwszy album studyjny południowokoreańskiej grupy GFriend , wydany 11 lipca 2016 roku przez wytwórnię Source Music i dystrybuowany przez LOEN Entertainment .
Głównym singlem z płyty jest „ Navillera ” ( kor .
너 그리고 나 ( NAVILLERA ) ) .
Sprzedał się w nakładzie egzemplarzy w Korei Południowej ( stan na czerwiec 2018 ) .
