LRT Televizija
LRT Televizija ( do 28 lipca 2012 jako LTV ) – pierwszy kanał Litewskiej Telewizji Publicznej nadawany przez Lietuvos Nacionalinis Radijas ir Televizija ( LRT ) od 30 kwietnia 1957 roku , a od 1975 emitowany w kolorze .
LRT nadaje ( od 22 września 2014 roku ) program całą dobę .
Wcześniej kanał nadawał średnio 19–20 godzin na dobę ( od 6:00 do ok. 1:00–2:00 czasu litewskiego ) .
Kanał jest dostępny na terenie Litwy oraz terenach przygranicznych krajów ościennych za pomocą nadajników naziemnych .
LRT nadaje też za pośrednictwem satelity Sirius 4 dzięki czemu jest dostępna w Europie Wschodniej , Centralnej i Północnej .
