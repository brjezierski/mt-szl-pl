La Chapelle-en-Juger
La Chapelle-en-Juger – miejscowość i dawna gmina we Francji , w regionie Normandia , w departamencie Manche .
W 2013 roku jej populacja wynosiła 661 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – La Chapelle-en-Juger oraz Hébécrevon – utworzono nową gminę Thèreval .
Siedzibą gminy została miejscowość Hébécrevon .
