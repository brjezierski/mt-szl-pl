La Chaux-de-Fonds
La Chaux-de-Fonds − miasto w Szwajcarii , w kantonie Neuchâtel , położone w górach Jury , kilka kilometrów od francuskiej granicy .
Po Genewie i Lozannie jest to trzecie pod względem wielkości miasto we francuskojęzycznej Szwajcarii .
W 2009 roku wraz z pobliskim miastem Le Locle wpisane na listę światowego dziedzictwa UNESCO .
