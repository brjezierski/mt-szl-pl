La Cuba ( Palermo )
La Cuba , także Castello della Cuba , Cuba Sottana i Palazzo della Cuba - pałac w Palermo z XII wieku .
Ukończony w 1180 r. jako rezydencja Wilhelma II Dobrego i miejsce królewskich uroczystości , był niegdyś otoczony wspaniałym parkiem z winnicami , sadami i dużym stawem rybnym .
Wybudowany w stylu arabsko-normańskim .
Nazwa pałacu pochodzi od arabskiego słowa `` qubba '' co oznacza `` łuk '' , `` sklepienie '' .
Pozostał we władaniu władców Sycylii do początku XIV wieku .
W 1575 r. , podczas zarazy , był używany jako szpital dla ofiar dżumy .
Później , w epoce Burbonów , został przekształcony w budynek koszarowy .
W 1921 r. zarządzanie Kubą zostało powierzone Ministerstwu Edukacji , następnie pod kierunkiem architekta Francesco Valenti rozpoczęto rekonstrukcję całego zespołu architektonicznego .
Obecnie jest to zabezpieczona ruina , w której wnętrzach zachowały się resztki dekoracji stalaktytowych ( mukarnas ) .
