La Hague
La Hague – gmina we Francji , w regionie Normandia , w departamencie Manche .
W 2013 roku populacja ludności wynosiła 11 840 mieszkańców .
Gmina została utworzona 1 stycznia 2017 roku z połączenia 19 ówczesnych gmin : Acqueville , Auderville , Beaumont-Hague , Biville , Branville-Hague , Digulleville , Éculleville , Flottemanville-Hague , Gréville-Hague , Herqueville , Jobourg , Omonville-la-Petite , Omonville-la-Rogue , Sainte-Croix-Hague , Saint-Germain-des-Vaux , Tonneville , Urville-Nacqueville , Vasteville oraz Vauville .
Siedzibą gminy została miejscowość Beaumont-Hague .
