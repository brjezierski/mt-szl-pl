La Linea
La Linea ( `` Linia , Balum Balum '' ) – włoski cykl krótkich filmów animowanych o przygodach bohatera żyjącego w świecie wykreowanym przez linię narysowaną tajemniczą ręką .
Na drodze ludzika wyrastały wciąż nowe przeszkody , a wszystkiemu towarzyszyła charakterystyczna muzyka `` balum , balum '' .
Serial emitowany był w Polsce w latach 80. w czasie niedzielnego programu 7 anten oraz w połowie lat 90. jako wypełniacz czasu antenowego , tzw .
„ zapchajdziura ” .
Na świecie emitowany był w ponad 40 krajach .
O postaci wrednego , krzykliwego a jednak sympatycznego człowieczka napisano 5 książek .
Postać z serialu została wykorzystana w ogromnej liczbie reklam na świecie – w Polsce m.in .
przez firmę Polkomtel w reklamie Simplusa oraz kilka sieci firm gastronomicznych .
Podobnej postaci użyto również w teledyskach piosenek `` The Riddle '' i `` Bla bla bla '' Gigi D ’ Agostino oraz `` ( Don ’ t ) Give Hate a Chance '' zespołu Jamiroquai .
