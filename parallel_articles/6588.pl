La Rochefoucauld ( Charente )
La Rochefoucauld – miejscowość i dawna gmina we Francji , w regionie Nowa Akwitania , w departamencie Charente .
W 2013 roku populacja ówczesnej gminy wynosiła 2959 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia dwóch ówczesnych gmin – La Rochefoucauld oraz Saint-Projet-Saint-Constant – powstała nowa gmina La Rochefoucauld-en-Angoumois .
Siedzibą gminy została miejscowość La Rochefoucauld .
