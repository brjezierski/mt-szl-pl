Laboulbenia calathi
Laboulbenia calathi T. Majewski – gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1994 r. Tomasz Majewski w Polsce na owadzie z rodziny biegaczowatych `` Calathus fuscipes '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( `` Coleoptera '' ) z rodziny biegaczowatych ( `` Carabidae '' ) należących do rodzaju `` Calathus '' .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
