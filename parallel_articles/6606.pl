Laboulbenia casnoniae
Laboulbenia casnoniae Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1891 r. Roland Thaxter w USA na owadzie z rodziny biegaczowatych `` Casnonia pennsylvanica '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( Coleoptera ) z rodziny biegaczowatych ( Carabidae ) należących do rodzajów `` Demetrias '' , `` Dromius '' , `` Metabletus '' , `` Microlestes '' i `` Casnonia '' .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
