Laboulbenia clivinalis
Laboulbenia clivinalis Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1899 r. Roland Thaxter na owadach z rodziny biegaczowatych `` Clivina collaris '' i `` Clivina fossor '' w Anglii .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
W Polsce Tomasz Majewski opisał jego występowanie na 2 gatunkach chrząszczy z rodziny biegaczowatych ( Carabidae ) : `` Clivina collaris '' i `` Clivina fossor '' .
