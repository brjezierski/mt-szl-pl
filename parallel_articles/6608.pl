Laboulbenia coneglanensis
Laboulbenia coneglanensis Speg .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1891 r. Carlo Luigi Spegazzini .
Synonimy :
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( Coleoptera ) z rodziny biegaczowatych ( Carabidae ) należących do rodzaju `` Harpalus '' .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
