Laboulbenia curtipes
Laboulbenia curtipes Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1892 r. Roland Thaxter na owadzie z rodziny biegaczowatych `` Bombidion bimaculatum '' w Waszyngtonie .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
W Polsce Tomasz Majewski opisał jego występowanie na 2 gatunkach chrząszczy z rodziny biegaczowatych ( Carabidae ) : `` Bombidion assimile '' i `` Bombidion varium '' .
