Laboulbenia dubia
Laboulbenia dubia Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1902 r. Roland Thaxter na owadzie `` Philonthus politus '' w Wielkiej Brytanii .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w 1994 i 2003 r. opisał jego występowanie na chrząszczach `` Philonthus cognatus '' i `` Philonthus mannerheimi '' z rodziny kusakowatych ( Staphylinidae ) .
