Laboulbenia filifera
Laboulbenia filifera Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1893 r. Roland Thaxter w USA na gatunkach owadów `` Anisodactylus harrisii '' , `` Anisodactylus nigerimus '' , `` Anisodactylus interpunctatus '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski opisał jego występowanie na chrząszczu `` Anisodactylus memorivagus '' z rodziny biegaczowatych ( Carabidae ) .
