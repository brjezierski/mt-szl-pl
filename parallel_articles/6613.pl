Laboulbenia flagellata
Laboulbenia flagellata Peyr .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał Johann Joseph Peyritsch w 1873 r. Synonimów ma 15 .
Są nimi wszystkie podgatunki , odmiany i formy .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( Coleoptera ) z rodziny biegaczowatych ( Carabidae ) należących do rodzajów `` Agonum '' , `` Badister '' , `` Loricera '' , `` Pterostichus '' .
Nie powoduje ich śmierci i zazwyczaj wyrządza im niewielkie szkody .
