Laboulbenia gyrinicola
Laboulbenia gyrinicola Speg .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał Carlo Luigi Spegazzini w 1914 r .
Synonim : `` Laboulbenia gyrinicola '' subsp .
`` stagnalis '' Speg .
1914 :
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( Coleoptera ) z rodziny krętakowatych ( Gyrinidae ) należących do rodzajów `` Gyrinus '' i `` Orectochilus '' .
Nie powoduje ich śmierci i zazwyczaj wyrządza im niewielkie szkody .
