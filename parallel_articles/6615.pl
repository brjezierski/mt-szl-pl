Laboulbenia inflata
Laboulbenia inflata Thaxt.– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1892 r. Roland Thaxter na chrząszczu z rodzaju `` Bembidion '' w Dakocie Południowej w USA .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( Coleoptera ) z rodziny biegaczowatych ( Carabidae ) należących do rodzajów `` Bembidion '' i `` Acupulpus '' .
Nie powoduje ich śmierci i zazwyczaj wyrządza mu niewielkie szkody .
