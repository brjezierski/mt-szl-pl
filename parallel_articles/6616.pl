Laboulbenia ophoni
Laboulbenia ophoni Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1889 r. Roland Thaxter na chrząszczach z rodzajów `` Ophonus '' i `` Harpalus '' w Algierii i Szwajcarii .
Synonimy :
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
W Polsce notowany na chrząszczach z rodziny biegaczowatych ( Carabidae ) : `` Agonum gracile '' , `` Harpalus affinis '' , `` Harpalus nitidulus '' , `` Harpalus seladnon '' .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
