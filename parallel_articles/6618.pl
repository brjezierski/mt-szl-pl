Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski , jedyny mykolog zajmujący się tą grupą grzybów na większą skalę , w 1994 i 2003 r. podaje występowanie `` Laboulbenia polyphaga '' na chrząszczach z rodziny biegaczowatych ( Carabidae ) : `` Amara apricaria '' , `` Amaria consularis '' i `` Trichotichmus laevicollis '' .
