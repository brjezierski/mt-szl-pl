Laboulbenia rougetii
Laboulbenia rougetii Mont .
& amp ; C.P .
Robin – gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisali w 1853 r. Jean Pierre François Camille Montagne i Charles Philippe Robin na owadach z rodzaju `` Brachinus '' .
Synonimy :
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje śmierci owada i zazwyczaj wyrządza mu niewielkie szkody .
W Polsce Tomasz Majewski w 1994 r. opisał jego występowanie na chrząszczu `` Brachinus crepitans '' z rodziny biegaczowatych ( Carabidae ) .
