Laboulbenia vulgaris
Laboulbenia vulgaris Peyr .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyb entomopatogeniczny ( pasożytujący na owadach ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laboulbenia , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Johann Joseph Peyritsch w 1873 r .
Wszystkie podgatunki i odmiany według Index Fungorum to synonimy gatunku typowego .
Charakterystyka .
Pasożyt zewnętrzny owadów .
Notowany na chrząszczach ( Coleoptera ) z rodziny biegaczowatych ( Carabidae ) : `` Bembidion '' , `` Deltomerus '' , `` Nebria '' , `` Trechoblemus '' , `` Trechus '' .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
