Lakówka ceglasta
Lakówka ceglasta ( `` Laccaria fraterna '' ( Sacc . )
Pegler ) – gatunek grzybów z rodziny piestróweczkowatych ( `` Hydnangiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Laccaria , Hydnangiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisany został w 1891 r. przez Pier Andrea Saccardo jako `` Naucoria fraterna '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu David Norman Pegler w 1965 r .
Nazwę polską nadali Barbara Gumińska i Władysław Wojewoda w 1968 r .
Morfologia .
