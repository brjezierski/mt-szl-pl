Lachnum fasciculare
Lachnum fasciculare Velen .
– gatunek grzybów należący do klasy patyczniaków ( `` Leotiomycetes '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lachnum , Lachnaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1934 r. Josef Velenovský .
Synonim : `` Dasyscyphus fascicularis '' ( Velen . )
Le Gal 1939 .
Morfologia .
Typu apotecjum ( miseczka ) , w eksykatach przeważnie na długich trzonkach , brązowe lub cieliste , pokryte bladobeżowo-brązowymi włoskami .
Trzonki często zniekształcone , tarczki ciemnobrązowe , czasem z winnym odcieniem .
Włoski brodawkowate , przeważnie cylindryczne lub tylko lekko główkowate , przeważnie z 3 septami , o długości ( 46– ) 56–86 × 3,2–5,6 pm .
Worki o wymiarach 42-54 × 3,6–5,4 μm , wyrastające z pastorałek .
Askospory wrzecionowate , 6,2–9,4 × 1,6–14,2 μm .
Parafizy lancetowate , o szerokości 2,4–4,2 µm , przewyższające worek o ok. 17–17 ( –20 ) μm .
`` Lachnum fasciculare '' od `` Lachnum pudibundum '' odróżnia się głownie tym , że jego worki wyrastają z pastorałków , od `` Lachnum crataegi '' głównie długością parafiz , dłuższymi trzonkami i bardziej gromadnym tworzeniem owocników .
Występowanie i siedlisko .
Podano występowanie `` Lachnum fasciculare '' w niektórych krajach Europy i na jednym stanowisku w ameryce Północnej .
Występuje również w Polsce ( w 2006 r. M. A. Chmiel przytoczyła 9 stanowisk ) .
Grzyb nadrzewny występujący na drewnie i gałązkach drzew liściastych .
