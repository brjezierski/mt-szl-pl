Lachnum pygmaeum
Lachnum pygmaeum ( Fr . )
Bres .
– gatunek grzybów należący do klasy patyczniaków ( `` Leotiomycetes '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lachnum , Lachnaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1822 r. Elias Fries , nadając mu nazwę `` Peziza pygmaea '' .
Obecną nazwę nadał mu Giacomo Bresàdola w 1903 r .
Ma około 30 synonimów .
