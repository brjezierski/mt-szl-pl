Skwierczyn Lacki
Skwierczyn Lacki – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Paprotnia .
Od przynajmniej wczesnego XV wieku wieś była w posiadaniu rodziny Skwierczyńskich herbu Ślepowron .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Bartłomieja Apostoła w Paprotni .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
