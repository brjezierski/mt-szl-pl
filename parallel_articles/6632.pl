Lactarius acutus
Lactarius acutus R. Heim – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
W Polsce nie występuje .
Charakterystyka .
Organizm mikoryzowy , rozwijający się w glebie i wytwarzający naziemne owocniki złożone z kapelusza i trzonu .
Miąższ zbudowany jest z kulistawych komórek powodujących ich specyficzną kruchość i nieregularny przełam .
Kapelusz owocników ma średnicę 2,2–3,5 cm i wgłębiony środek ze średnim , ostrym garbkiem , jest ciemno zabarwiony , o gładkiej , tłustawej w dotyku powierzchni .
Hymenofor blaszkowy .
Trzon o długości 3–5 cm i średnicy 0,5–0,7 cm , barwy kapelusza .
Miąższ biały , czasami ochrowawy w kapeluszu , o rybiej woni .
Zarodniki elipsoidalne , o wymiarach 9–10,5×7,5–8,5 μm , o brodawkowato-siateczkowanej , amyloidalnej powierzchni .
