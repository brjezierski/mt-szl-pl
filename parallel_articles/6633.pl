Lactarius adhaerens
Lactarius adhaerens R. Heim – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
W Polsce nie występuje .
Charakterystyka .
Grzyby mikoryzowe , rozwijające się w glebie i wytwarzające naziemne owocniki złożone z kapelusza i trzonu , których miąższ zbudowany jest z kulistawych komórek powodujących ich specyficzną kruchość i nieregularny przełam .
Kapelusz owocników ma średnicę 4–4,5 cm i wgłębiony środek , jest złotawo lub żółto zabarwiony .
Hymenofor blaszkowy , blaszki jasnozłotobrązowe , o regularnej tramie .
Trzon o długości 4–6 cm i średnicy 0,5–0,6 cm , cylindryczny , barwy ceglastobrązowy .
Miąższ zwarty , w kapeluszu biały , w trzonie kremowy , wytwarzający białe mleczko .
Podstawki przeważnie czterozarodnikowe , czasem dwuzarodnikowe .
Zarodniki kulistawe , o średnicy 7,3–9 μm , pokryte siateczką o wysokości ok. 2,5 µm , amyloidalne .
Wysyp zarodników jasnokremowy .
