Mleczaj lśniący
Mleczaj lśniący ( `` Lactarius albocarneus '' Britzelm . )
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten został zdiagnozowany taksonomicznie przez Maxa Britzelmayra w `` „ Botanisches Zentralblatt ” '' z 1895 .
Nazwę polską podała Alina Skirgiełło w 1998 .
Synonimy łacińskie :
Morfologia .
Średnicy 3–7 cm , u dojrzałych owocników płaski i nieco wklęsły na środku , na obrzeżach zazwyczaj pofalowany .
Brzeg u młodych owocników podwinięty , u starszych wyprostowany .
Powierzchnia o barwie jasnej , żółtawoszarej lub szarocielistej , u młodych owocników zazwyczaj z fioletowawym odcieniem , u starszych ochrowa , a miejscami nawet brązowoszara .
Po uciśnięciu zmienia barwę na czarniawoszarą .
Brzeg nieco jaśniejszy .
Powierzchnia kapelusza jest silnie śluzowata i lepka , podczas suszy błyszcząca lub srebrzysto oszroniona .
