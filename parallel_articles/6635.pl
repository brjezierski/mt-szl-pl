Lactarius amarus
Lactarius amarus R. Heim – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
W Polsce nie występuje .
Charakterystyka .
Grzyby mikoryzowe , rozwijające się w glebie i wytwarzające naziemne owocniki złożone z kapelusza i trzonu , których miąższ zbudowany jest z kulistawych komórek powodujących ich specyficzną kruchość i nieregularny przełam .
Kapelusz owocników ma pokrój szerokorozpostarty , średnicę 4–4,5 cm , jest złotawo lub żółto zabarwiony , jaśniej , koncentrycznie strefowany .
Hymenofor blaszkowy , blaszki gęsto rozstawione .
Trzon krótki , stożkowaty , o długości ok. 2 cm i średnicy przy kapeluszu ok. 1 cm , zwężający się i zaostrzony przy podstawie .
Miąższ zwarty , kremowy , bez zapachu , wytwarzający białe , ostro-gorzkie mleczko .
Zarodniki elipsoidalne , o wymiarach 7,8–9×6,9–7,7 μm , pokryte brodawkami częściowo połączonymi siateczką o wysokości ok. 0,8–1,5 µm , amyloidalne .
Wysyp zarodników biały .
