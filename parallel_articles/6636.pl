Lactifluus annulatoangustifolius
Lactifluus annulatoangustifolius ( Beeli ) Buyck – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
W Polsce nie występuje .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lactifluus , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek `` Lactifluus annulatoangustifolius '' został po raz pierwszy zdiagnozowany taksonomicznie przez Maurice ' a Beeliego ( jako `` Russula annulatoangustifolia '' ) w `` Bulletin du Jardin botanique de l'État à Bruxelles '' z 1936 r .
Synonimy :
Charakterystyka .
Grzyby mykoryzowe rozwijające się w glebie i wytwarzające naziemne owocniki złożone z kapelusza i trzonu , których miąższ zbudowany jest z kulistawych komórek powodujących ich specyficzną kruchość i nieregularny przełam .
Kapelusz o pokroju wypukło-płasko-wgłębionym , średnicy ok. 5 cm , żółtawo zabarwiony , mięsisty , cienki i błyszczący .
Hymenofor blaszkowy , blaszki białe , równe , przyrośnięte do trzonu .
Trzon cylindryczny , o długości ok. 3,5 cm i średnicy ok. 1 cm , białawy .
Miąższ biały , o gorzkim smaku .
Zarodniki kuliste , o średnicy 8–10 μm , bezbarwne ( hialinowe ) , pokryte brodawkami połączonymi siateczką .
