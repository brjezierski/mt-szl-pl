Lactarius atlanticus
Lactarius atlanticus Bon – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Morfologia .
Średnicy 3–6 ( do 8 ) cm , jednorodnie pomarańczowo-czerwony lub rdzawo-czerwony , początkowo o pokroju wypukłym , potem rozpostartym , czasem z garbkiem , o podwiniętym brzegu .
Pokryty gładką , suchą , matową skórką .
Blaszkowy , blaszki ochrowo-żółte , po dojrzeniu czerwieniejące i pokryte białym nalotem zarodników , przyrośnięte do trzonu .
Długości 5–7 cm i średnicy 0,1–1 cm .
Zbudowany jest z kulistawych komórek powodujących ich specyficzną kruchość i nieregularny przełam .
Blady , kremowo-ochrowy , nie wybarwiający się w żywicy gwajakowej , o ostrym smaku i woni liści cykorii lub bluszczu pospolitego , wydzielający gorzkie , białawe mleczko .
Kulistawe , o średnicy 7–8 μm , pokryte amyloidalną siateczką .
Występowanie .
Organizm mikoryzowy , rozwijający się w glebie .
W Polsce nie występuje .
