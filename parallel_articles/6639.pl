Mleczaj złocisty
Mleczaj złocisty ( `` Lactarius chrysorrheus '' Fr . )
– gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lactarius , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda w 1983 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też pod nazwami : bedłka tygrysowa , podrydzyk ostry , podrydzyk psi .
Synonimy naukowe : .
Morfologia .
Średnica 4–6 ( 9 ) cm , kształt początkowo wypukły szybko jednak rozpłaszcza się i w końcu jest staje się , w środku zagłębiony do wklęsłego .
Brzeg początkowo podwinięty , później rozpostarty , ostry .
Powierzchnia gładka , w stanie wilgotnym śliska .
Barwa od jasno mięsistej do jasno-ochrowo-pomarańczowej .
Posiada kilka ciemniejszych , wyraźnych lub rozmytych pręg .
Blaszki z międzyblaszkami , przy trzonie przyrośnięte , czasami rozwidlone .
Mają barwę jasnopłową pomarańczowym odcieniem .
