Mleczaj jodłowy
Mleczaj jodłowy ( `` Lactarius salmonicolor '' R. Heim & amp ; Leclair ) – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lactarius , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Nazwę polską podał Władysław Wojewoda w 1999 r. , dawniej w polskim piśmiennictwie mykologicznym gatunek ten opisywany był jako mleczaj późnojesienny .
Synonimy naukowe :
Morfologia .
Średnica 5–15 cm , u młodych okazów wypukły , potem płaski z wklęsłym środkiem .
Powierzchnia barwy pomarańczowożółtej , brzoskwiniowożółtej lub pomarańczowej , koncentrycznie strefowana rudopomarańczowymi pręgami .
Podczas wilgotnej pogody mogą pojawiać się winno-brązowe plamy .
Nie występuje barwa zielona , jedynie u starszych owocników może pojawiać się zielonawy odcień .
Brzeg podwinięty i nieco oszroniony , ale u starszych owocników staje się gładki
