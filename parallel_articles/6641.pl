Urodziła się i dorastała w Nowym Yorku .
Pierwsze kroki w muzyce zaczęła stawiać w wieku młodzieńczym , kiedy to rozpoczęła naukę gry pianinie i zaczęła komponować utwory .
Karierę zaczęła pisaniem piosenek dla innych artystów , takich jak Britney Spears czy Jennifer Lopez , w tym samym czasie próbując nawiązać współpracę z wytwórnią muzyczną .
W 2008 wydała swój debiutancki album studyjny pt .
`` The Fame '' , który okazał się być ogromnym sukcesem , przede wszystkim dzięki singlom „ Just Dance ” oraz „ Poker Face ” .
Kolejnym dziełem Gagi został minialbum pt .
The Fame Monster , `` czyli '' kontynuacja debiutanckiego albumu , zawierająca m.in .
utwory : „ Bad Romance ” , „ Telephone ” oraz „ Alejandro ” ; które osiągnęły międzynarodowy sukces .
W 2011 Lady Gaga wydała swój drugi album studyjny pt .
`` Born This Way '' .
Główny singel albumu o tym samym tytule osiągnął szczyt list w wielu krajach i został najszybciej sprzedającą się piosenką w serwisie iTunes , odnotowując ponad milion pobrań w mniej niż tydzień .
W 2013 roku został wydany czwarty album studyjny piosenkarki – `` Artpop '' .
W 2014 wydała album pt .
`` Cheek to Cheek '' , który nagrała wraz z Tonym Bennettem W 2016 wydała piąty album pt .
`` Joanne '' .
W 2017 wystąpiła podczas muzycznej przerwy Super Bowl oraz na festiwalu Coachella .
W 2018 wypuszczono na rynek ścieżkę dźwiękową do filmu `` Narodziny gwiazdy '' .
Płyta oraz na czele z piosenką „ Shallow ” osiągnęła duży sukces , dzięki czemu Gaga stała się pierwszą artystką mającą pięć albumów numer jeden w XXI wieku w Stanach Zjednoczonych .
W 2020 wydała szósty album studyjny pt .
`` Chromatica '' , którego jednym z singli był międzynarodowy hit „ Rain on Me ” .
W 2021 wydała siódmy album pt .
`` Love for Sale '' , który powstał przy współpracy z Tonym Bennettem .
W 2022 została wydana ścieżka dzwiękowa do filmu `` , '' której Gaga była jednym z głównych twórców .
Oprócz kariery muzycznej Gaga jest zaangażowana w projekty filmowe .
Swój debiut miała w 2001 , kiedy zagrała nieznaczną rolę w serialu Rodzina Soprano .
Następnie pojawiła się w kilku projekcjach filmowych , w roli Cameo W latach 2015–2016 była częścią obsady serialu `` American Horror Story '' .
W 2018 roku miała miejsce premiera filmu Narodziny gwiazdy , w którym Gaga zagrała swoją pierwszą główną rolę na dużym ekranie .
W 2021 doszło do premiery filmu `` Dom Gucci '' w którym Gaga wcieliła się w główną rolę obok Adama Drivera .
Za swoje role aktorskie otrzymała : nominacje do Oscara , nagrodę Złoty Glob , dwie nominacje do Nagrody Brytyjskiej Akademii Filmowej , dwie nominacje do Nagrody Gildii Aktorów Ekranowych oraz nagrodę Critics ’ Choice Movie Awards .
Mając na koncie 27 mln sprzedanych albumów oraz 146 mln singli , jest jedną z najlepiej sprzedających się artystów na świecie .
Za swoje projekty muzyczne otrzymała : Oscara , Złotego Globa , 13 nagród Grammy , 3 statuetki BRIT , 18 nagród MTV Video Music Awards oraz nominację do Fryderyka .
Sześć wydanych przez nią płyt znalazło się na pierwszym miejscu list Billboard 200 , zaś siedemnaście singlów zostało uwzględnionych w pierwszej dziesiątce utworów listy `` Billboard '' Hot 100 .
Została wpisana 13 razy do `` Księgi rekordów Guinnessa '' .
W 2015 została ogłoszona „ kobietą roku ” przez magazyn `` Billboard '' .
