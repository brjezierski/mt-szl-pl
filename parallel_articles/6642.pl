Lady Pank ( album )
Lady Pank – debiutancki album studyjny polskiego zespołu muzycznego Lady Pank , wydany 20 czerwca 1983 roku na LP nakładem Tonpressu .
W późniejszych latach doczekał się licznych reedycji na CC i CD .
W 2017 roku Agencja Artystyczna MTJ wznowiła album na LP .
Album jest uważany za najważniejszy w dorobku zespołu , a także za jeden z najważniejszych w historii polskiego rocka .
Jest jedynym polskim wydawnictwem , z którego aż sześć utworów dotarło do pierwszego miejsca Listy przebojów Programu Trzeciego .
`` Lady Pank '' został sprzedany w milionowym nakładzie i zyskał status złotej płyty .
W marcu 1985 roku , w trakcie pobytu w Stanach Zjednoczonych , zespół nagrał anglojęzyczną wersję albumu ze zmienionymi tekstami utworów – `` Drop Everything '' .
W 2018 roku ukazał się album `` LP1 '' , zawierający pochodzące z debiutanckiej płyty utwory ( oraz trzy dodatkowe – „ Mała Lady Punk ” , „ Tańcz głupia tańcz ” i „ Minus 10 w Rio ” ) , nagrane na nowo i zaśpiewane z udziałem wielu zaproszonych gości .
