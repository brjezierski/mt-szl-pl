Lagarto
Lagarto – miasto i gmina w Brazylii , leży w stanie Sergipe .
Gmina zajmuje powierzchnię 968,92 km² .
Według danych szacunkowych w 2016 roku miasto liczyło 103 188 mieszkańców .
Położone jest około 70 km na zachód od stolicy stanu , Aracaju , oraz około 1400 km na północny wschód od Brasílii , stolicy kraju .
W 2014 roku wśród mieszkańców gminy PKB per capita wynosił 12 124,97 reais brazylijskich .
