Lagos
Lagos – miasto w południowo-zachodniej Nigerii , położone nad Zatoką Gwinejską .
Jest to największe miasto w państwie .
W 2022 roku Lagos zamieszkiwało 15,388 mln mieszkańców .
Jedno z najszybciej rozwijających się metropolii na świecie .
W Lagos mieści się Makoko , największy slums na świecie .
Historia .
Założone pod koniec XV wieku przez Portugalczyków .
W wiekach XVII-XIX jeden z ważniejszych punktów wywozu niewolników .
