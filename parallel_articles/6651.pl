Lahaur
Lahaur ( ; , `` Lāhaur '' ; , `` Lahaur '' ) – miasto we wschodnim Pakistanie , stolica prowincji Pendżab , na zachodnim przedgórzu Himalajów , nad rzeką Rawi ( dopływ Ćanabu ) , w pobliżu granicy z Indiami .
Drugie po Karaczi miasto kraju , główny ośrodek gospodarki Pendżabu .
W 2017 roku liczyło ponad 11,1 mln mieszkańców .
Gospodarka .
Ośrodek przemysłu włókienniczo-odzieżowego oraz rzemiosła .
Historia i kultura .
Centrum Sikhów , w I połowie XIX wieku stolica ich państwa .
W XVIII–XIX wieku ośrodek malarstwa miniaturowego .
Siedziba najstarszego w kraju uniwersytetu , założonego w 1882 .
W roku 1990 odbyły się tutaj VII Mistrzostwa Świata w hokeju na trawie .
Zabytki .
Fort i Ogrody Szalimar w Lahaurze zostały wpisane na listę światowego dziedzictwa UNESCO .
Budowa fortu rozpoczęła się w XI wieku , a zakończyła w XVI wieku .
Fort położony jest na wzgórzu .
Z bramy wyjściowej fortu przechodzi się do jednego z największych meczetu Badśahi .
O budowie ogrodów Śalimar w stylu perskim zdecydował w 1642 roku Szahdżahan .
W ogrodzie znajduje się ponad 400 fontann , pawilony , 12 bram wejściowych , alejki i trawniki .
Organizowane są w nim wystawy artystów , przyjęcia dla dyplomatów i koncerty .
