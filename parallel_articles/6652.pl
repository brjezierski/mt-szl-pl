Lahti
Lahti ( szw .
`` Lahtis '' ) – miasto w południowej Finlandii , nad jeziorem Vesijärvi , na skraju Pojezierza Fińskiego .
Jedno z większych w kraju .
Słowo `` lahti '' znaczy po fińsku `` zatoka '' .
Sport .
Lahti jest znanym ośrodkiem narciarstwa .
Rokrocznie odbywają się tutaj zawody Pucharu Świata w biegach narciarskich , skokach narciarskich , kombinacji norweskiej oraz biathlonie .
Miasto posiada kompleks trzech skoczni o nazwie Salpausselkä ; zachowały się również starsze skocznie drewniane - na jednej z nich Stanisław Marusarz zdobył w 1938 roku wicemistrzostwo świata .
W mieście tym aż siedmiokrotnie odbyły się Mistrzostwa świata w narciarstwie klasycznym : w latach 1926 , 1938 , 1958 , 1978 , 1989 , 2001 i 2017 .
W 2019 roku odbyły się tu Mistrzostwa świata juniorów w narciarstwie klasycznym.W 2021 roku odbyły się tu Mistrzostwa świata juniorów w kombinacji norweskiej i skokach narciarskich .
Z miasta pochodzi sławny fiński skoczek Janne Ahonen .
W Lahti wraz z żoną mieszka również inny słynny Fin , trener skoków narciarskich Hannu Lepistö - polskim kibicom znany jako szkoleniowiec Adama Małysza .
Lahti jest siedzibą klubu narciarskiego Lahden Hiihtoseura , klubu hokejowego Pelicans Lahti ( na co dzień występuje on w rozgrywkach SM-liiga ) , a także klubu piłkarskiego FC Lahti .
Kultura .
Z Lahti pochodzi grupa muzyczna Korpiklaani .
Komunikacja .
W mieście znajduje się stacja kolejowa Lahti .
