Lipsk ( rejon hancewicki )
Lipsk ( ; ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie hancewickim , w sielsowiecie Malkowicze .
W XIX w. opisywany jako miejscowość w najdzikszej i najodludniejszej okolicy Pińszczyzny .
Jeszcze w 1924 do wsi nie dochodziła żadna utrzymywana droga .
W dwudziestoleciu międzywojennym leżał w Polsce , w województwie poleskim , w powiecie łuninieckim , do 18 kwietnia 1928 w gminie Łunin , następnie w gminie Czuczewicze .
