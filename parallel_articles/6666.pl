Lalendorf ( stacja kolejowa )
Lalendorf ( ) – stacja kolejowa w Lalendorf , w kraju związkowym Meklemburgia-Pomorze Przednie , w Niemczech .
Znajduje się na skrzyżowaniu linii Bützow – Szczecin i Neustrelitz – Warnemünde .
Według DB Station & amp ; Service ma kategorię 7 .
Stacja .
Stacja znajduje się w gminie Lalendorf w powiecie Rostock , około 10 kilometrów na wschód od Güstrow i około 40 kilometrów na południowy wschód od Rostoku .
Linia kolejowa z Bützow biegnie w kierunku wschodnio-zachodnim , linia kolejowa z Neustrelitz do Warnemünde z południowego wschodu na północny zachód .
