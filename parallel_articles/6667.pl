Lama andyjska
Lama andyjska , dawniej : lama ( `` Lama glama '' ) – udomowiony gatunek ssaka parzystokopytnego z rodziny wielbłądowatych ( Camelidae ) .
Występuje w Ameryce Południowej .
Lamy wykorzystywane są głównie jako zwierzęta juczne oraz do produkcji wełny i mięsa .
Dorosłe lamy osiągają do 250 kg wagi i 119 cm wysokości w kłębie .
Lamy są zwierzętami socjalnymi , z rozbudowanymi zachowaniami grupowymi .
Taksonomia .
Gatunek po raz pierwszy zgodnie z zasadami nazewnictwa binominalnego opisał w 1758 roku szwedzki przyrodnik Karol Linneusz nadając mu nazwę `` Camelus Glama '' .
Holotyp pochodził z Andów , w Peru .
Lineusz swój opis oparł na wcześniejszych opracowaniach różnych autorów dotyczących udomowionych stad .
Ewolucja i systematyka .
Uważa się , że przodkowie lam wyewoluowali około 40 milionów lat temu , na równinach Ameryki północnej .
Podobnie jak przodkowie koniowatych , rozprzestrzeniły się stamtąd poprzez pomost lądowy do Starego Świata .
Jednak w przeciwieństwie do koniowatych , nie wymarły w Nowym Świecie .
Badania paleontologiczne wykazały , że plemiona Lamini ( lamy ) i Camelini ( wielbłądy ) rozdzieliły się 11 milionów lat temu , jednakże badania molekularne przesunęły to wydarzenie na 25 milionów lat temu .
Lamy wraz z innymi gatunkami wielbłądowatych Starego i Nowego Świata są jedynymi żyjącymi obecnie przedstawicielami podrzędu Talypoda .
`` Lama glama '' została wyhodowana selektywnie z `` L. guanicoe '' w celu wykorzystania jej jako zwierzęta juczne oraz do produkcji mięsa .
Udomowienie lamy miało miejsce 4000–4500 lat temu w południowo-środkowych Andach ( półniocne Chile i północno-zachodnia Argentyna ) i 4500–5500 lat temu w środkowych Andach ( Junín de los Andes , Argentyna ) .
Szczątki osteologiczne i analiza DNA dokumentują pochodzenie udomowienia w obrębie rozmieszczenia północnego podgatunku `` L. guanicoe '' , `` L. g. cacsilensis '' .
Nie rozpoznano podgatunków , ale istnieją dwie odrębne rasy fenotypowe : lamy o krótkiej wełnie orazy lamie o długiej wełnie .
Nazewnictwo zwyczajowe .
W polskiej literaturze zoologicznej dla oznaczenia gatunku używana była nazwa zwyczajowa „ lama ” .
Jednak w wydanej w 2015 roku przez Muzeum i Instytut Zoologii Polskiej Akademii Nauk publikacji „ Polskie nazewnictwo ssaków świata ” gatunkowi przypisano oznaczenie lama andyjska , rezerwując nazwę lama dla rodzaju `` Lama '' .
