Lampedusa
Lampedusa ( wł .
`` Isola Di Lampedusa '' , syc .
`` Isula di Lampidusa '' , łac .
`` Lopadussa '' ) – włoska wyspa pochodzenia wulkanicznego na Morzu Śródziemnym ( leżąca pomiędzy Maltą i Tunezją ) , największa z Wysp Pelagijskich o powierzchni 20,2 km² .
Jej długość wynosi 9 km , a szerokość 3 km .
Wysokość n.p.m .
sięga 133 m. Administracyjnie wyspa należy do sycylijskiej gminy Lampedusa e Linosa w prowincji Agrigento .
Pochodzenie nazwy .
Nazwa `` Lampedusa '' wywodzi się z greckiej starożytnej nazwy wyspy Λοπαδούσσα lub Λαπαδούσσα ( Lopadoússa/Lapadoússa ) .
Uważa się , że nazwa pochodzi od słowa λέπας ( `` lépas '' ) , które oznacza „ skała ” , ze względu na skalisty krajobraz wyspy ; to samo słowo było używane przez Greków na określenie pewnego rodzaju ostryg , dlatego wyspa mogła otrzymać nazwę z powodu obfitości tego rodzaju ostryg .
Inni uczeni uważają , że nazwa wywodzi się od słowa λαμπάς ( `` lampás '' ) , które oznacza „ pochodnię ” , ponieważ na wyspie umieszczono światła służące żeglarzom .
Historia .
W starożytności wyspa była miejscem postoju i bazą morską dla Fenicjan , Greków , Rzymian i Berberów .
Rzymianie założyli tu wytwórnię cenionego sosu rybnego zwanego `` garum '' .
W 813 została zdobyta przez Saracenów .
W 1553 piraci berberyjscy z Afryki Północnej najechali wyspę i uprowadzili do niewoli 1000 mieszkańców .
W czerwcu 1943 , w czasie II wojny światowej , przed inwazją aliantów na Sycylię , wyspę zabezpieczono bez napotkania oporu w czasie operacji Corkscrew siłami niszczyciela HMS Lookout i 95 żołnierzy z 2 batalionu Coldstream Guards .
15 kwietnia 1986 Libia wystrzeliła dwa pociski Scud w kierunku stacji nawigacyjnej LORAN-C bazy United States Coast Guard znajdującej się na wyspie w odpowiedzi na amerykańskie bombardowania Trypolisu i Bengazi .
Pociski minęły wyspę i wylądowały w morzu nie wyrządzając żadnych zniszczeń .
Populacja .
Populacja wyspy to ok. 6,3 tys .
mieszkańców .
Większość ludności zamieszkuje miasteczko we wschodniej części wyspy .
W miasteczku znajduje się mały port i lotnisko .
W zachodniej części wyspy znajduje się stacja nadawcza systemu nawigacyjnego LORAN-C o mocy 325 kW , z masztem wysokości 190,5 m .
Gospodarka .
Na wyspie znajduje się 30 zakładów przetwórstwa rybnego .
Wokół wyspy poławia się tuńczyki , sardynki i sardele .
W południowej części wyspy znajduje się port , wioska i lotnisko .
Imigranci .
Wyspa leży na szlaku morskim , przez który do Europy docierają nielegalni imigranci z Afryki .
8 lipca 2013 papież Franciszek w swojej pierwszej podróży zagranicznej przybył na wyspę i oddał hołd imigrantom , którzy utonęli w okolicach Lampedusy .
Geografia .
Geograficznie , wyspa jest częścią kontynentu afrykańskiego , choć politycznie jest częścią Włoch i Europy .
Najwyższym punktem wyspy jest Monte Albero del Sole ( 133 m n.p.m ) .
Wyspa należy od 2002 do obszaru ochronnego Area Marina Protetta „ Isole pelagie ” .
W jej pobliżu , na wysepce `` Isola dei Conigli '' ( Wyspa Królicza ) , składają jaja żółwie morskie karetta ( `` Caretta caretta '' ) .
W pęknięciach skał i na wydmach rosną niewielkie stapelie ( `` Caralluma europaea '' ) oraz kwitnące na żółto-fioletowo astry ( `` Reichardia tingitana '' ) .
