Lanciano
Lanciano – miejscowość i gmina we Włoszech w regionie Abruzja , w prowincji Chieti .
Według danych na rok 2004 gminę zamieszkiwały 35 713 osoby , 541,1 os./km² .
Według Kościoła katolickiego w VIII wieku w Lanciano miał miejsce pierwszy potwierdzony cud eucharystyczny .
W sanktuarium w Lanciano przechowywane są relikwie uważane za relikwie ciała i krwi Chrystusa .
