Landsberg am Lech
Landsberg am Lech – miasto powiatowe w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Monachium , siedziba powiatu Landsberg am Lech .
Leży nad rzeką Lech , przy autostradzie A96 , drodze B17 i liniach kolejowych Monachium – Memmingen i Bobingen - Weilheim in Oberbayern .
Najbliżej położone duże miasta : Augsburg , Monachium ok. 50 km na wschód , Norymberga - ok. 200 km na północny wschód i Stuttgart - ok. 200 km na północny zachód .
Nie zostało zburzone podczas drugiej wojny światowej .
W tutejszym więzieniu po nieudanym puczu monachijskim przebywał Adolf Hitler .
