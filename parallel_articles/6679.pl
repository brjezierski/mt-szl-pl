Park Krajobrazowy Beskidu Śląskiego
Park Krajobrazowy Beskidu Śląskiego – park krajobrazowy położony w województwie śląskim .
Obejmuje porośnięte lasem pasma górskie Beskidu Śląskiego : pasmo Czantorii oraz Baraniej Góry .
Park zajmuje powierzchnię 38 620 ha , natomiast jego otulina liczy 22 285 ha .
Na terenie parku znajdują się źródła Białej i Czarnej Wisełki – dwóch potoków dających początek Wiśle .
Geologia .
Budowa geologiczna wzniesień Beskidu Śląskiego jest zróżnicowana .
Wzgórza zbudowane są głównie z łupków i wapieni .
Występują tu również warstwy zbudowane z piaskowców godulskich i zlepieńców istebniańskich .
Na terenie parku znajdują się liczne jaskinie fliszowe , z których część ma status pomników przyrody .
Na terenie parku znajdują się m.in .
Jaskinia w Trzech Kopcach ( najdłuższa niekrasowa jaskinia w Polsce ) , Jaskinia Malinowska , Jaskinia Lodowa w Szczyrku , Jaskinia Mokra .
Dla ochrony jaskiń został utworzony w gminie Lipowa rezerwat przyrody Kuźnie .
W parku znajdują się też ostańce skalne : ambony , grzyby i grzędy , wśród nich Malinowska Skała czy Skały Grzybowe w Paśmie Stożka .
