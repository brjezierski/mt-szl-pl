Lansana Conté
Lansana Contè ( ur .
1934 w Dubréce , zm .
22 grudnia 2008 w Konakry ) – prezydent Gwinei od 2 kwietnia 1984 do śmierci 22 grudnia 2008 , muzułmanin , przedstawiciel grupy etnicznej Soussou .
Młodość .
Conte , urodzony w Moussayah Loumbaya ( Dubreka ) , został wykształcony w lokalnej szkole koranicznej , po czym uczęszczał do szkoły w Dubrece , a następnie rozpoczął studia w szkole wojskowej w Bingerville ( Wybrzeże Kości Słoniowej ) i Saint Louis ( Senegal ) .
Wojsko i działalność polityczna .
W 1955 Conte wstąpił do armii francuskiej i został wysłany do Algierii , walcząc tam w wojnie algierskiej ( w 1957 ) .
Po służbie w szeregach francuskich powrócił do Gwinei , która uzyskała 2 października 1958 niepodległość z rąk Francji .
Został wcielony do nowo utworzonej armii , z rangą sierżanta .
W 1962 uczęszczał do Camp Alpha , szkoły oficerskiej w Konakry .
Wkrótce potem został przeniesiony do 2 .
