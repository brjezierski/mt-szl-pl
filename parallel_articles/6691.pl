Laos
Laos , oficjalnie Laotańska Republika Ludowo-Demokratyczna ( `` Lao '' , `` Sathalanalat Paxathipatai Paxaxôn Lao '' ) – państwo w Azji Południowo-Wschodniej , na Półwyspie Indochińskim .
Stolicą jest Wientian ( 700 tys .
mieszkańców ) , główna rzeka Mekong .
Na północy Laos graniczy z Mjanmą i Chinami , na wschodzie z Wietnamem , na południu z Kambodżą , a na zachodzie z Tajlandią .
Laos jest krajem socjalistycznym .
Geografia .
Laos znajduje się na Półwyspie Indochińskim w południowo-wschodniej Azji .
Jest jedynym krajem regionu pozbawionym dostępu do morza .
Od północy graniczy z Chinami , od wschodu z Wietnamem , od południa z Kambodżą i Tajlandią , od zachodu z Mjanmą .
Jest krajem prawie w całości porośniętym lasem .
Na północy kraju rozciągają się liczne wyżyny i góry .
Najwyższym szczytem jest Phou Bia ( 2817 m n.p.m . ) .
Na północnym wschodzie tereny wyżynne przechodzą w Równinę Dzbanów ( fr .
`` Plaine Des Jarres '' ) , romboidalnego kształtu obszar żyznych ziem zarzucony wielkimi , kamiennymi urnami grzebalnymi datowanymi na tysiące lat wstecz .
Na południu rozciąga się wąski pas lądu opadającego ku płaskowyżowi Bolovens .
W czasach kolonialnych rozciągały się tu liczne plantacje kawy i francuskie ośrodki wypoczynkowe .
Rzeka Mekong w znacznej części jest rzeką graniczną z Tajlandią .
W Laosie panuje klimat monsunowy – od końca maja do końca października trwa pora gwałtownych deszczy tropikalnych , zaś od grudnia do kwietnia pora sucha z wysokimi temperaturami .
Krótka wiosna i jesień wypełniają czas pomiędzy tymi dwiema porami .
