( 1504 ) Lappeenranta – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 3 lat i 263 dni w średniej odległości 2,4 au .
Została odkryta 23 marca 1939 roku w Obserwatorium Iso-Heikkilä w Turku przez Liisi Otermę .
Nazwa planetoidy pochodzi od Lappeenranty , miasta w Finlandii .
Przed nadaniem nazwy planetoida nosiła oznaczenie tymczasowe ( 1504 ) 1939 FM .
