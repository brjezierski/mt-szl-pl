Lara Flynn Boyle
Lara Flynn Boyle ( ur .
24 marca 1970 w Davenport ) – amerykańska aktorka filmowa i telewizyjna pochodzenia irlandzkiego ze strony ojca i włosko-irlandzko-niemieckiego ze strony matki .
Życiorys .
Wczesne lata .
Urodziła się w Davenport w stanie Iowa w rodzinie rzymskokatolickiej jako córka Sally Boyle i Michaela L. Boyle ’ a .
Jej imię zostało zaczerpnięte z powieści Borisa Pasternaka `` Doktor Żywago '' .
Jej dziadek Charles Augustus Boyle był Amerykaninem pochodzenia włoskiego .
W 1976 jej rodzice rozwiedli się .
