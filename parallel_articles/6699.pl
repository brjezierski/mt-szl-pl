Laragne-Montéglin
Laragne-Montéglin – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Wysokie .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 3371 osób , a gęstość zaludnienia wynosiła 143 osoby/km² .
W styczniu 2015 r. Laragne-Montéglin zamieszkiwało 3687 osób , przy gęstości zaludnienia wynoszącej 156,8 osób/km² .
