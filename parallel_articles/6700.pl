Laramie ( Wyoming )
Laramie – stolica hrabstwa Albany w stanie Wyoming , w Stanach Zjednoczonych .
Miasto zostało nazwane od nazwiska francuskiego trapera Jacques ' a Laramie , który zaginął w okolicznych górach , nazwanych Laramie Mountains .
