Pniarek lekarski
Pniarek lekarski ( `` Fomitopsis officinalis '' ( Vill . )
Bondartsev & amp ; Singer ) – gatunek grzybów z rodziny pniarkowatych ( `` Fomitopsidaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Fomitopsis , Fomitopsidaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1788 roku francuski botanik Dominique Villars nadając mu nazwę `` Boletus officinalis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1941 roku Appollinaris Semenovich Bondartsev i Rolf Singer , przenosząc go do rodzaju `` Fomitopsis '' .
Synonimy nazwy naukowej :
Polską nazwę pniarek lekarski nadał Władysław Wojewoda w 1999 r. Gatunek opisywany był także pod innymi nazwami : modrzewnik lekarski ; huba lekarska , modrzewiowa , niedźwiedzia , gąbka modrzewowa i agaryk modrzewiowy
Morfologia .
Wieloletnie owocniki mogące żyć nawet do 80 lat i osiągające do 1 metra wysokości i wagę do 10 kg .
Największy znany owocnik ma 82 warstwy przyrostu i jest przechowywany w Uniwersytecie w Waszyngtonie .
Szare , z czasem kruche , popękane .
Przyrośnięte bokiem do pnia , brak trzonu , zwykle kopytowate , po wielu latach cylindryczne , czasami owocniki pozrastane ze sobą .
Miąższ biały , kruchy , serowaty i bardzo gorzki .
