Las Palmas de Gran Canaria
Las Palmas ( formalnie Las Palmas de Gran Canaria ) – miasto w archipelagu i regionie Wyspy Kanaryjskie , w Hiszpanii , leżące nad Oceanem Atlantyckim .
Jest największym miastem i stolicą wyspy Gran Canaria , prowincji Las Palmas oraz regionu Wysp Kanaryjskich ( wraz z Santa Cruz de Tenerife ) .
Jest ono położone na północnym wschodzie wyspy Gran Canaria .
Jest ósmym co do wielkości miastem Hiszpanii .
Centrum administracyjne ma 381 223 mieszkańców na obszarze 100,55 km² , natomiast zespół miejski ma 670 000 mieszkańców będąc piątym co do wielkości zespołem miejskim w Hiszpanii .
Miasto znajduje się w strefie klimatu subtropikalnego typu półpustynnego , z całorocznym okresem z letnimi temperaturami .
Średnia roczna temperatura wynosi 24 °C w dzień i 18 °C w nocy .
