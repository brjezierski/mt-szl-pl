Lasarte-Oria
Lasarte-Oria – miasto w hiszpańskiej prowincji Gipuzkoa/Guipúzcoa , w Kraju Basków .
Według ostatniego spisu ludności miasto zamieszkiwane jest przez 17 856 osób .
Leży około 8,5 km na południowy zachód od stolicy prowincji - San Sebastián i około 445 km na północ od Madrytu .
W 1965 roku odbyły się tutaj 38. mistrzostwa świata w kolarstwie szosowym .
