Latvijas Televīzija
Latvijas Televīzija ( LTV ) – łotewska telewizja publiczna , istniejąca od 1954 .
Nadaje dwa kanały : LTV1 ( tylko w języku łotewskim ) , będący odpowiednikiem polskiej TVP1 , oraz skierowany do młodzieży LTV7 ( także po rosyjsku ) .
LTV było organizatorem odbywającego się w 2003 w Rydze Konkursu Piosenki Eurowizji oraz w 2017 Chóru Roku Eurowizji .
Firma jest finansowana z dotacji rządu łotewskiego ( około 60 % ) , a pozostała część pochodzi z reklam telewizyjnych .
Chociaż przeniesienie LTV na finansowanie opłat abonamentowych było od dawna dyskutowane , rząd konsekwentnie się temu sprzeciwiał .
