Lozanna
Lozanna ( fr. , niem .
`` Lausanne '' ; wł. , retorom .
`` Losanna '' ) – miasto w Szwajcarii , stolica kantonu Vaud , nad Jeziorem Genewskim , ok. 64 km na północny wschód od Genewy , ok. 100 km na południe od Berna .
Według danych z 2018 miasto liczyło 138 905 mieszkańców ( czwarte pod względem liczby ludności miasto Szwajcarii ) .
Obszar aglomeracji miasta liczy około 400 000 mieszkańców .
Miasto jest węzłem drogowym i kolejowym , funkcjonuje tu również port lotniczy Lozanna-La Blecherette .
Historia .
Jako osada `` Lousonna '' otoczona drewnianą palisadą , znana od czasów Cesarstwa Rzymskiego .
