Lautoka
Lautoka – trzecie pod względem liczby ludności miasto Fidżi , położone w zachodniej części wyspy Viti Levu ( prowincja Ba ) .
Według danych szacunkowych na rok 2009 liczy 54 356 mieszkańców .
Miasto jest stolicą Dystryktu Zachodniego , leży w prowincji Ba .
W mieście rozwinięty jest przemysł cukrowniczy .
Pierwsza fabryka powstała tu pod koniec XIX wieku .
Była nią cukrownia założona w 1899 przez Colonial Sugar Refining Company , a która rozpoczęła produkcję w 1903 .
Lautoka otrzymała prawa miejskie 25 lutego 1977 .
Na początku lat 90. żyło tu ok. 31 000 osób .
W Lautoce Queens Road zamienia się w Kings Road .
24 km na południe od miasta znajduje się port lotniczy Nadi .
