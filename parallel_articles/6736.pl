Lawrenceville ( Georgia )
Lawrenceville – miasto w Stanach Zjednoczonych , w stanie Georgia , siedziba hrabstwa Gwinnett .
Należy do obszaru metropolitalnego Atlanty .
Swoją siedzibę główną ma tutaj Kościół Prezbiteriański w Ameryce .
Demografia .
Według spisu w 2020 roku liczy 30,6 tys mieszkańców , oraz ma silnie zróżnicowaną populację , gdzie 39 % stanowią osoby białe ( 30,5 % nie licząc Latynosów ) , 38 % to czarnoskórzy lub Afroamerykanie , 7,3 % było rasy mieszanej i 6,8 % to Azjaci .
Pochodzenie latynoskie deklarowało 21,3 % populacji .
