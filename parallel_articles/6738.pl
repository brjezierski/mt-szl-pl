Lay ’ s
Lay ’ s – marka chipsów ziemniaczanych należąca do Frito-Lay ( części PepsiCo ) od 1965 .
Historia .
Produkcję chipsów zapoczątkował w 1932 roku Herman W. Lay , który w 1938 roku po przejęciu Barrett Food Company utworzył H.W .
Lay & amp ; Company .
W czasie II wojny światowej spożycie chipsów znacznie wzrosło ( w dużej mierze z powodu racjonowania cukru i czekolady ) i firma Hermana Laya stała się znaczącym producentem na rynku .
Po wojnie Lay zautomatyzował produkcję , a także rozpoczął współpracę z Charlesem Elmerem Doolinem , właścicielem The Frito Company , którą Lay przejął w 1961 roku , dwa lata po śmierci Doolina , tworząc w ten sposób firmę Frito-Lay .
Cztery lata później firma połączyła się z Pepsi Cola Company , tworząc PepsiCo .
Marka Lay ’ s wkrótce stała się jedną z najbardziej rozpoznawanych marek chipsów , podobnie jak Frito-Lay , który pod koniec lat 60. stał się wiodącym światowym producentem przekąsek i pozostaje nim do dziś .
W niektórych krajach marka Lay ’ s występuje pod inną nazwą , np .
w Wielkiej Brytanii jako Walkers , a w Brazylii – Elma Chips .
