Hawr
Hawr ( fr .
`` Le Havre '' ) – miejscowość i gmina we Francji , w regionie Normandia , w departamencie Sekwana Nadmorska .
Według danych na rok 2009 gminę zamieszkiwały 179 783 osoby , a gęstość zaludnienia wynosiła 3830 osób/km² ( wśród 1421 gmin Górnej Normandii Hawr plasuje się na 1. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 2 .
) .
Hawr ma charakter miasta przemysłowego .
Znajduje się tam największy port we Francji ( 5. co do wielkości w Europie ) , a także jedna z kilku największych francuskich rafinerii – Raffinerie de Normandie ( Total – Elf ) .
W 1984 utworzono uniwersytet o charakterze międzynarodowym , gdzie naukę pobierają studenci z całego świata .
Ich liczba wynosi około 7000 .
