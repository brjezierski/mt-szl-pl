Le Mans ( film )
Le Mans – amerykański sportowy film akcji w reżyserii Lee H. Katzina wydany 23 czerwca 1971 roku .
Fabuła .
Film opowiada o zmaganiach kierowców podczas najsłynniejszego wyścigu wytrzymałościowego na świecie ; 24h Le Mans .
W 1971 roku głównymi pretendentami do wygrania wyścigu były ekipy Porsche oraz Ferrari .
Kierowcy tych zespołów będą robić wszystko , by wygrać , będą jechać na granicy bezpieczeństwa oraz zaryzykują własnym życiem .
Lider zespołu Porsche , Michael Delaney podczas poprzedniej edycji wyścigu uległ wypadkowi , w którym zginął również kierowca Ferrari .
Będzie musiał rywalizować nie tylko z przeciwnikami , ale i z własnym strachem i zmęczeniem .
