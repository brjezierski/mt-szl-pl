Lebiedzie ( przystanek kolejowy )
Lebiedzie ( ; , ros .
nazwa normatywna Лебеди ) – przystanek kolejowy w pobliżu miejscowości Lebiedzie , w rejonie zelwieńskim , w obwodzie grodzieńskim , na Białorusi .
Położony jest na linii Baranowicze - Wołkowysk .
Przystanek powstał w latach 30 .
XX w. w miejscu istniejącej wcześniej mijanki .
