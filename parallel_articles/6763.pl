Misecznica jaśniejsza
Misecznica jaśniejsza ( `` Lecanora chlarotera '' Nyl . )
– gatunek grzybów z rodziny misecznicowatych ( `` Lecanoraceae '' ) .
Ze względu na symbiozę z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
