Lecce ( stacja kolejowa )
Lecce ( włoski : `` Stazione di Lecce '' ) – stacja kolejowa w Lecce , w prowincji Lecce , w regionie Apulia , we Włoszech .
Znajduje się na linii Adriatica , Martina Franca - Lecce i Lecce - Gallipoli .
Stacja znajduje się na piazzale Oronzo Massari , niedaleko od centrum miasta .
Jest zarządzana przez Rete Ferroviaria Italiana , natomiast budynek przez Centostazioni .
Stacja jest obsługiwana przez pociągi Trenitalia i od 13 grudnia 2009 przez Ferrovie del Nord Barese .
