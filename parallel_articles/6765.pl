Koźlarek grabowy
Koźlarek grabowy , koźlarz grabowy ( `` Leccinellum pseudoscabrum '' ( Kallenb . )
Mikšik ) – gatunek grzybów z rodziny borowikowatych ( `` Boletaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Leccinellum , Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1929 r. Franz Joseph Kallenbach nadając mu nazwę `` Boletus pseudoscaber '' .
W 2017 roku został przeniesiony do rodzaju `` Leccinellum '' przez czeskiego mykologa Michala Mikšika .
Niektóre synonimy naukowe :
Nazwę „ koźlarz grabowy ” podała Alina Skirgiełło w 1960 r. ( dla `` L. duriusculum '' ) .
W 2021 r. Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała używanie nazwy „ koźlarek grabowy ” .
Morfologia .
Średnica 7–14 cm , kształt początkowo półkulisty , później płasko rozpostarty , i poduchowaty .
Powierzchnia naga , jamista i często pomarszczona , a u starszych okazów popękana na poletka , zwłaszcza przy długotrwałej suchej pogodzie .
Barwa w odcieniach brązu : żółtobrązowa , siwobrązowa , oliwkowobrązowa , ciemnobrązowa , czarnobrązowa .
