Lech Kaczyński
( ur .
18 czerwca 1949 w Warszawie , zm .
10 kwietnia 2010 w Smoleńsku ) – polski polityk i prawnik .
Prezydent Rzeczypospolitej Polskiej w latach 2005–2010 .
Brat bliźniak Jarosława Kaczyńskiego .
Działacz opozycji politycznej w okresie Polskiej Rzeczypospolitej Ludowej .
Senator I kadencji ( 1989–1991 ) oraz poseł na Sejm I i IV kadencji ( 1991–1993 , 2001–2002 ) .
Prezes Najwyższej Izby Kontroli ( 1992–1995 ) , minister sprawiedliwości i prokurator generalny w rządzie Jerzego Buzka ( 2000–2001 ) , prezydent miasta stołecznego Warszawy ( 2002–2005 ) .
Współzałożyciel partii Prawo i Sprawiedliwość i jej pierwszy prezes ( 2001–2003 ) .
Był doktorem habilitowanym nauk prawnych , profesorem nadzwyczajnym Uniwersytetu Kardynała Stefana Wyszyńskiego w Warszawie i Uniwersytetu Gdańskiego , specjalistą w zakresie prawa pracy .
Zginął w katastrofie rządowego samolotu Tu-154 .
Życiorys .
Dzieciństwo .
Urodził się 18 czerwca 1949 w domu rodzinnym przy ul .
Pawła Suzina 3 na warszawskim Żoliborzu .
Jego ojciec , Rajmund Kaczyński , z zawodu inżynier , był żołnierzem Armii Krajowej , uczestnikiem powstania warszawskiego , kawalerem Orderu Virtuti Militari .
Matka , Jadwiga z domu Jasiewicz , z wykształcenia była filologiem polskim ( zawodowo związana z Instytutem Badań Literackich Polskiej Akademii Nauk ) .
Przez matkę był potomkiem rodów szlacheckich z XVI wieku : Olszowskich , Biskupskich i Kurozwęckich , ze strony ojca – potomkiem Kaczyńskich herbu Pomian .
Był bratem bliźniaczym Jarosława Kaczyńskiego .
Przy ich porodzie asystowała jako położna matka poety Tadeusza Gajcego .
