Lech ( piwo )
Lech – marka piwa produkowanego przez browar Lech Browary Wielkopolski należący do Kompanii Piwowarskiej .
Osiągnięcia i nagrody .
W 2013 Lech był siódmą , a w 2015 czwartą najlepiej sprzedającą się marką piwa w Polsce pod względem wartości sprzedaży .
11 marca 2007 roku w nowo otwartej części CHSiB Stary Browar w Poznaniu postawiono największy na świecie kufel wypełnionym piwem Lech Pils .
Ustanowiono wtedy rekord Guinnessa w największej liczbie osób pijących z jednego kufla , w którym znajdowało się 4250 litrów tego piwa .
W ustanowieniu rekordu wzięło udział 10 625 osób .
