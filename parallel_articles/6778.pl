Legionowo ( stacja kolejowa )
Legionowo – stacja kolejowa położona w centrum miasta Legionowo .
Opis stacji .
Na stacji znajdują się dwa szerokie , wysokie ( 760 mm pgs ) perony wyspowe .
Oba są pokryte kostką brukową .
Na peronach znajdują się : tablice z nazwą stacji , wiaty przystankowe z ławkami , megafony , rozkłady jazdy pociągów , kosze na śmieci .
Wejście na perony możliwe jest od ulicy Kościuszki i Piłsudskiego ( od strony centrum ) , a także od drugiej strony torów , tj .
od strony ulicy Szwajcarskiej .
Obie strony torów i perony łączy obecnie przejście podziemne przystosowane do obsługi osób niepełnosprawnych .
Do 2008 roku przejście na drugą stronę torów było możliwe poprzez istniejące przejścia naziemne ( łączące perony ) oraz nadziemną kładkę dla pieszych .
Ich likwidacja została wymuszona z powodu przystosowania infrastruktury kolejowej do prowadzenia pociągów przez odcinek w Legionowie z prędkością 140 km/h .
Na stacji znajdował się niegdyś dodatkowy nieużywany peron boczny .
