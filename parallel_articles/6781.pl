Leicester City F.C .
Leicester City Football Club – angielski klub piłkarski , mający swoją siedzibę w Leicester , grający na King Power Stadium .
Od sezonu 2014/2015 występuje w Premier League po zdobyciu mistrzostwa Football League Championship w sezonie 2013/2014 .
Mistrz Anglii w sezonie 2015/2016 .
Klub został założony w 1884 roku jako Leicester Fosse , grał na boisku w pobliżu Fosse Road .
Nazwa Leicester City została przyjęta w 1919 roku .
W 1891 drużyna przeniosła się na Filbert Street , gdzie grała przez 111 lat , zanim w 2002 roku przeprowadziła się na Walkers Stadium ( Obecnie King Power Stadium ) .
Pierwszym sukcesem klubu było zajęcie pierwszego miejsca w rozgrywkach drugiej ligi angielskiej ( obecnie Football League Championship ) w 1925 roku , czego dokonał później jeszcze pięciokrotnie .
Leicester trzykrotnie zwyciężał w Pucharze Ligi Angielskiej ( najwięcej razy spośród klubów które nigdy nie wygrały Pucharu Anglii ) , a także jeden raz w Football League One ( trzecia klasa rozgrywkowa ) .
W sezonie 2013/2014 wygrali rozgrywki Championship , a w 2016 roku zdobyli mistrzostwo Anglii .
Historia .
Klub został założony w 1884 roku przez grupę chłopców ze Szkoły Wyggeston jako Leicester Fosse .
W 1890 dołączył do The Football Association .
W 1891 roku klub dołączył do Midland League , a następnie w 1894 roku został wybrany do Division Two of Football League ( Second Division ) .
W swoim pierwszym ligowym meczu Leicester przegrał 4:3 z Grimsby , po czym w następnym tygodniu odniósł pierwsze zwycięstwo w meczu przeciwko Rotherham na własnym stadionie .
