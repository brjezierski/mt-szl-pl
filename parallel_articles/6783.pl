Lejda
Lejda ( ) – miasto w zachodniej Holandii , w prowincji Holandia Południowa u wybrzeża Morza Północnego .
Należy do najstarszych miast niderlandzkich .
Jest miejscem urodzenia malarza Rembrandta .
Prawa miejskie otrzymało w 1266 .
Jest jednym z głównych ośrodków handlowych Holandii ; handel kwiatami , przemysł maszynowy , kosmetyczny , poligraficzny oraz włókienniczy .
W Lejdzie mieści się najstarszy holenderski uniwersytet , założony w 1575 roku , w którym wynaleziono kondensator – butelkę lejdejską .
Mieszczą się tu również ważne instytucje naukowo-badawcze i towarzystwa naukowe : Biblioteka Uniwersytecka ( założona w 1575 ) , Biblioteka Literatury Niderlandzkiej ( 1766 ) , obserwatorium astronomiczne w Lejdzie .
W mieście znajdują się stacje kolejowe Leiden Centraal , Leiden Lammenschans i De Vink .
