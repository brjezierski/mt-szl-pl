Leimen ( Badenia-Wirtembergia )
Leimen – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Rhein-Neckar , w powiecie Rhein-Neckar-Kreis .
Leży po części w Parku Natury Neckartal-Odenwald , ok. 6 km na południe od Heidelbergu , przy drodze krajowej B3 i linii kolejowej Mannheim–Karlsruhe .
