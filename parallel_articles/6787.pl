Lekarzewice
Lekarzewice – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie radziejowskim , w gminie Osięciny .
Podział administracyjny .
Wieś królewska , położona w II połowie XVI wieku w powiecie brzeskokujawskim województwa brzeskokujawskiego , należała do starostwa brzeskokujawskiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa włocławskiego .
Wieś sołecka – zobacz jednostki pomocnicze gminy w BIP .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 120 mieszkańców .
Jest 26. co do wielkości miejscowością gminy Osięciny .
