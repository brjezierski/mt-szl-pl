Lelant
Lelant , IPA /lə'lænt/ – wieś w Wielkiej Brytanii , w Kornwalii , nad Oceanem Atlantyckim nad zatoką St Ives Bay , przy linii kolejowej St Ives bay line .
St Erth - St Ives .
W r. 1998 zamieszkiwało ją 4230 osób .
Wieś istnieje od średniowiecza , choć nie wymieniono jej w Domesday Book .
W średniowieczu wieś była ważnym portem , którego ruiny pozostały do dziś .
Obecnie wieś jest miejscowością wypoczynkową z piaszczystą plażą .
Leży na szklaku turystycznym South West Coast Path .
