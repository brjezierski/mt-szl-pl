Twardówka lejkowata
Twardówka lejkowata ( `` Lentinellus micheneri '' ( Berk .
& amp ; M.A .
Curtis ) Pegler ) – gatunek grzybów z rodziny szyszkogłówkowatych ( `` Auriscalpiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lentinellus , Auriscalpiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1853 r. Miles Joseph Berkeley i Moses Ashley Curtis nadając mu nazwę `` Lentinus micheneri '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1983 r. D.N .
Pegler .
Ma 17 synonimów .
Niektóre z nich :
Nazwę polską nadał Władysław Wojewoda w 2003 r. ( dla synonimu `` Lentinellus omphalodes '' ) .
Morfologia .
Szerokość 1–5 cm , w młodości wypukły lub poduszeczkowaty z centralnym zagłębieniem , później szeroko wypukły lub prawie płaski , z głębokim zagłębieniem lub bez .
Powierzchnia w stanie wilgotnym gładka , różowawo brązowa lub jaśniejsza , podczas wysychania wyraźnie zmienia kolor , wskutek czego kapelusz staje się dwubarwny .
Brzeg prosty .
Przyrośnięte do trzonu , dość rzadkie , białawe do różowawo brązowych , nie zmieniające barwy po uszkodzeniu .
Ostrza wyraźnie ząbkowane .
