Twardówka filcowata
Twardówka filcowata , twardówka kasztanowa ( `` Lentinellus ursinus '' ( Fr . )
Kühner ) – gatunek grzybów z rodziny szyszkogłówkowatych ( `` Auriscalpiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lentinellus , Auriscalpiaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1821 r. Elias Fries nadając mu nazwę `` Agaricus ursinus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1926 r. Robert Kühner .
Ma 13 synonimów .
Niektóre z nich :
Naukowa nazwa rodzajowa pochodzi od słowa „ lentus ” oznaczającego „ elastyczny ” i „ ellusa ” ( zdrobnienie ) .
Nazwa gatunkowa pochodzi od słowa „ ursus ” oznaczającego niedźwiedzia .
Obydwie polskie nazwy nadali Barbara Gumińska i Władysław Wojewoda w 1988 r. Nie są one tłumaczeniem nazwy naukowej , lecz nawiązują do cech tego grzyba .
