Lentini ( stacja kolejowa )
Lentini ( wł .
`` Stazione di Lentini '' ) – stacja kolejowa w Lentini , w prowincji Syrakuzy , w regionie Sycylia , we Włoszech .
Stacja znajduje się na linii Mesyna – Syrakuzy .
Według klasyfikacji RFI ma kategorią `` brązową '' .
Historia .
Stacja Lentini została wybudowana przez Società Vittorio Emanuele i Società per le strade ferrate della Sicilia , znane również jako `` Rete Sicula '' .
Była to część projektu połączenia Syrakuz z miastem i portem w Katanii , aby przekazywać produkty rolne , a zwłaszcza cytrusy z Niziny Katańskiej .
Stacja została wybudowana na północnych obrzeżach miasta , w pobliżu Lago di Lentini i została otwarta 1 lipca 1869 wraz z otwarciem linii kolejowej ze stacji Catania Centrale .
19 stycznia 1871 został również połączona z Syrakuzami z otwarciem do ruchu ostatniego odcinka 57,8 km do stacji Siracusa .
