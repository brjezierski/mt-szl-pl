Twardoskórzak pucharowaty
Twardoskórzak pucharowaty , twardziak pucharkowaty ( `` Neolentinus cyathiformis '' ( Schaeff . )
Della Maggiora & amp ; Trassinelli ) – gatunek grzybów z rodziny niszczycowatych ( `` Gloeophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neolentinus , Gloeophyllaceae , Gloeophyllales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1774 r. Jacob Christian Schaeffer nadając mu nazwę `` Agaricus cyathiformis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2014 r. Della Maggiora i Trassinelli , przenosząc go do rodzaju `` Neolentinus '' .
Synonimów naukowych ma około 20 .
Niektóre z nich :
Władysław Wojewoda w 2003 r. zaproponował polską nazwę twardziak pucharkowaty .
Wówczas gatunek ten klasyfikowany był do rodzaju `` Lentinus '' ( twardziak ) .
W polskim piśmiennictwie mykologicznym opisywany był również jako twardziak kielichowaty .
