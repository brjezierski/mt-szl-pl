Łyczak szczeciniasty
Łyczak szczeciniasty ( `` Panus neostrigosus '' Drechsler-Santos & amp ; Wartchow ) – gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Panus , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy naukowe :
Istniało zamieszanie w nazewnictwie tego gatunku i jego synonimach .
Aby to uporządkować Drechsler-Santos i Wartchow w 2012 r. wprowadzili nową nazwę gatunkową .
Nazwę polską „ twardziak szczeciniasty ” zaproponował Władysław Wojewoda w 2003 r. Po przeniesieniu gatunku do rodzaju `` Panus '' ( łyczak ) stała się ona niespójna z nazwą naukową .
W 2021 r. Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała używanie nazwy „ łyczak szczeciniasty ” .
Morfologia .
Obok blaszkowca drobnozarodnikowego ( `` Lenzites betulina '' ) jest w rzędzie żagwiowców ( `` Polyporales '' ) jednym z nielicznych gatunków o blaszkowatym hymenoforze .
