Landwarów ( gmina )
Gmina Landwarów ( lit .
`` Lentvario seniūnija '' ) – gmina w rejonie trockim okręgu wileńskiego na Litwie , położona w rejonie etnograficznym Dzukija .
Ośrodkiem administracji jest miasto Landwarów .
