Leoben
Leoben – miasto powiatowe w środkowej Austrii , w kraju związkowym Styria , siedziba powiatu Leoben .
Leży nad Murą .
Liczy 24 680 mieszkańców ( 1 stycznia 2015 ) .
Historia .
Wzmiankowane po raz pierwszy w 982 roku .
Leoben rozwijało się dzięki korzystnemu położeniu na skrzyżowaniu szlaków handlowych oraz wydobyciu i przetwórstwu rud żelaza .
Od 1173 r. posiadało targ , od 1415 prawo składu .
W 1797 r. podpisano w Leoben preliminaria pokojowe między Francją i Austrią ( pokój w Campo Formio ) .
