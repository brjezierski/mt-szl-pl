Leon Goretzka
Leon Goretzka ( ur .
6 lutego 1995 w Bochum ) – niemiecki piłkarz , występujący na pozycji pomocnika w niemieckim klubie Bayern Monachium oraz w reprezentacji Niemiec .
Wychowanek WSV Bochum 06 .
Kariera klubowa .
VfL Bochum .
Goretzka jest wychowankiem klubu WSV Bochum 06 .
W 2001 dołączył do VfL Bochum .
W jego drużynach młodzieżowych występował do 2012 .
1 lipca 2012 został włączony do kadry pierwszego zespołu .
W rozgrywkach 2 .
Bundesligi zadebiutował 4 sierpnia 2012 w meczu przeciwko Dynamu Drezno ( 2:1 ) .
W tym meczu zdobył bramkę na 1:1 .
Sezon 2012/2013 zakończył w lidze z 32 meczami w których strzelił 4 gole .
Budził zainteresowanie m.in .
FC Schalke 04 , Realu Madryt , czy Arsenalu Ostatecznie zdecydował się na odejście do tego pierwszego .
FC Schalke 04 .
2 lipca 2013 został piłkarzem FC Schalke 04 .
Kwota transferu wyniosła około 3,25 miliona euro .
Jego pierwszym meczem w karierze w Bundeslidze było spotkanie przeciwko Hamburger SV ( 3:3 ) , które zostało rozegrane w Gelsenkirchen 11 sierpnia 2013 .
Bayern Monachium .
1 lipca 2018 ogłoszono przejście Goretzki do Bayernu Monachium , z którym podpisał czteroletni kontrakt .
W klubie zadebiutował 12 sierpnia w wygranym 5:0 meczu Superpucharu Niemiec z Eintrachtem Frankfurt .
1 września w spotkaniu Bundesligi z VfB Stuttgart ( 3:0 ) strzelił premierowego gola dla Bayernu .
