Leonard Cohen
Leonard Norman Cohen ( ur .
21 września 1934 w Montrealu , zm .
7 listopada 2016 w Los Angeles ) – kanadyjski poeta , pisarz i piosenkarz tworzący w stylu piosenki autorskiej w gatunku folk rock .
Życiorys .
Urodził się w 1934 roku w Montrealu jako Leonard Norman Cohen , syn Mashy ( Marshy ) i Nathana Cohenów , w średniozamożnej żydowskiej rodzinie .
Ojciec pochodził z Polski , a matka , Masha Klonitsky , była córką talmudysty , rabbiego Solomona Klonitsky-Kline , Żyda litewskiego pochodzenia .
15 sierpnia 1937 roku zmarł dziadek Leonarda ze strony ojca , Lyon Cohen , człowiek aktywny w społeczności kanadyjskich Żydów .
W 1944 zmarł ojciec Leonarda Nathan .
Dzięki swojej rodzinie otrzymał solidną edukację na poziomie średnim i zaczął studia na anglojęzycznym Uniwersytecie McGilla w rodzinnym mieście .
Mimo że był przeciętnym studentem , jeszcze w trakcie studiów zdobył prestiżową nagrodę `` McNaughton Prize '' za osiągnięcia literackie .
5 października 1955 ukończył studia na Uniwersytecie McGilla uzyskując stopień bakałarza sztuk ( `` Bachelor of Arts '' ) .
W maju 1956 roku wydał swój pierwszy tom poezji `` Let Us Compare Mythologies '' .
Na przestrzeni następnych lat pisał i publikował poezję oraz wydał dwie powieści : `` The Favorite Game '' 1963 oraz `` Beautiful Losers '' w 1966 r .
Leonard Cohen w wywiadach często powtarzał , że zaczął śpiewać , gdyż trudno mu było wyżyć z pisania .
Biografowie nieco inaczej przedstawiają jego życie w tamtym okresie .
Honoraria literackie , rządowe granty oraz dochody z rodzinnego przedsiębiorstwa wystarczały na wygodne życie .
Leonard Cohen w tym czasie dużo podróżował , prowadził intensywne życie towarzyskie i erotyczne .
Lubił otaczać się pięknymi kobietami i , dopóki było to legalne , eksperymentował z LSD .
Choć nauczył się grać na gitarze w wieku trzynastu lat , a wcześniej zaczął pisać piosenki , początkowo traktował muzykę w sposób czysto rekreacyjny i nie zamierzał zostać piosenkarzem .
Nigdy też nie był wybitnym instrumentalistą .
