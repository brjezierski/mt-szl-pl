Leonberg
Leonberg – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Stuttgart , w powiecie Böblingen .
Leży ok. 14 km na północ od Böblingen , przy autostradach A8 , A81 i drodze krajowej B295 .
W mieście znajdują się stacja kolejowa Leonberg i przystanek kolejowy Höfingen .
