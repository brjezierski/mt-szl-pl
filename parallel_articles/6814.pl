Leonding
Leonding – miasto w Austrii , w kraju związkowym Górna Austria , w powiecie Linz-Land .
Leży nad Dunajem .
Liczy 26934 mieszkańców ( 2015 ) .
W latach 1898-1905 w mieście wraz z rodzicami mieszkał Adolf Hitler .
Rodzice jego spoczywają na tutejszym cmentarzu .
W roku 2012 ich grób został zlikwidowany , bez ekshumacji szczątków , z powodu traktowania go jako miejsce kultu przez neonazistów .
W mieście rozwinął się przemysł maszynowy oraz elektrotechniczny .
