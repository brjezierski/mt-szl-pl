Łeonid Kuczma
Łeonid Danyłowycz Kuczma , ukr .
Леонід Данилович Кучма ( ur .
9 sierpnia 1938 w Czajkinach ) – ukraiński polityk , inżynier i działacz komunistyczny , kandydat nauk technicznych .
Premier Ukrainy w latach 1992–1993 oraz prezydent Ukrainy w latach 1994–2005 , pełniący tę funkcję przez dwie kadencje .
W latach 1990–1994 deputowany do Rady Najwyższej USRR i Rady Najwyższej Ukrainy .
