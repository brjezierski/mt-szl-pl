Leopold Moczygemba
Leopold Moczygemba OFMConv , ( ur .
18 października 1824 w Płużnicy Wielkiej , zm .
23 lutego 1891 w Dearborn ) – franciszkanin konwentualny , założyciel miejscowości Panna Maria w Teksasie , w 1854 .
Wysłannik papieski do Ameryki i założyciel w 1879 polskiego Seminarium św .
Cyryla i Metodego w Orchard Lake , przeznaczonego pierwotnie dla Polaków .
Życiorys .
Leopold był synem Leopolda , karczmarza , i Ewy Krawiec , córki chłopa z Boguszyc .
Uczył się w gimnazjach w Gliwicach i Opolu .
Następnie wyjechał do Włoch , gdzie 17 listopada 1843 w miejscowości Osimo ( Prowincja Ankona ) wstąpił do zakonu franciszkanów .
Nowicjat odbył w klasztorze Santa Vittoria delle Fratte w Osimo .
18 listopada 1844 roku złożył śluby zakonne , a 25 lipca 1847 w Pesaro przyjął święcenia kapłańskie .
Ze względu na wiek prokurator generalny zakonu musiał wystarać się o dyspensę na przyjęcie święceń .
Później wyjechał do południowych Niemiec , do klasztoru Oggersheim .
Tu spotkał Jean-Marie Odina , biskupa diecezji Galveston w Teksasie , któremu polecił go o. Bonaventura Keller i namówił do podjęcia misji w Stanach Zjednoczonych .
Po przeprowadzce do Teksasu Leopold rozpoczął działalność duszpasterską wśród Niemców mieszkających w okolicach San Antonio ( m.in .
w New Braunfels ) .
Następnie o. Leopold Moczygemba kupił ziemię w hrabstwie Karnes , koło San Antonio i sprowadził grupę Górnoślązaków , pochodzących z Płużnicy Wielkiej koło Strzelec Opolskich .
Na wezwanie ojca Leopolda przybyło do Teksasu łącznie ok. 2300 osób .
Pionierzy ( ok. 150 osób ) , którzy dotarli już w 1854 roku , założyli na kupionej przez Leopolda ziemi osadę Panna Maria .
Jej symbolicznym początkiem była msza , jaką ksiądz Moczygemba odprawił dla przybyłych Górnoślązaków w Wigilię 1854 roku .
Narastające rozczarowanie warunkami życia w Teksasie sprawiło , że o. Moczygemba , w obawie przed samosądem , salwował się ucieczką na północ Stanów Zjednoczonych .
Podczas swojego życia czynnie wspierał Górnoślązaków i innych Polaków w północno-centralnych Stanach Zjednoczonych .
