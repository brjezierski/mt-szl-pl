Leopold Szersznik
Leopold Jan Szersznik , inne formy nazwiska : `` Scherschnik ; Šeršnik '' ( ur .
3 marca 1747 w Cieszynie , zm .
21 stycznia 1814 tamże ) – ksiądz katolicki , jezuita , historyk , pedagog , slawista , bibliofil , fundator biblioteki i pionier muzealnictwa .
Pochodzenie .
Leopold Szersznik pochodził z rodziny , której przedstawiciele byli notowani na Śląsku Cieszyńskim od połowy XVII wieku .
Bratem jego dziadka był Jerzy Szersznik , drugi mąż Magdaleny , dobrodziejki ubogich i fundatorki wieży kościelnej w Cieszynie .
Dziadek Leopolda , Andrzej Szersznik ( zm .
po 1756 ) , był książęcym leśnikiem w Skoczowie .
Leopold był najstarszym z piątki dzieci Jana Antoniego Szersznika , syndyka miejskiego w Cieszynie , i jego pierwszej żony Joanny Alojzy ( burmistrzanka cieszyńska ) , córki Leopolda Innocentego Polzera , prawnika i burmistrza Cieszyna .
Życiorys .
Urodzony w Cieszynie , tam też pobierał pierwsze nauki w szkołach jezuickich ( 1756-1762 ) .
Następnie studiował filozofię w Ołomuńcu ( od roku 1762 , a dwa lata później – 9 października 1764 – otrzymał tytuł „ primus philosophiae magister ” i wkrótce wstąpił do jezuitów ) , nauki humanistyczne w czeskich Brzeźnicach , a w Pradze ( od roku 1768 ) matematykę , historię kościoła oraz hebrajski i grecki ( równocześnie pracując w uczelnianej bibliotece ) .
W międzyczasie odbył 2-letni nowicjat w Brnie .
W latach 1770–1772 nauczał gramatyki w czeskim Chebie .
Do roku 1773 był jezuitą , następnie księdzem świeckim .
W roku 1777 został profesorem gimnazjum w Cieszynie , a w następnym roku jego prefektem .
W roku 1804 został nadzorcą szkół katolickich w Księstwie Cieszyńskim , walczył o wprowadzenie podręczników polskich w początkowym nauczaniu młodzieży .
