Gąsówka bladoniebieskawa
Gąsówka bladoniebieskawa ( `` Lepista glaucocana '' ( Schumach . )
Singer ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lepista , Incertae sedis , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1881 r. G. Bresàdola jako `` Tricholoma glaucocanum '' , do rodzaju `` Lepista '' przeniósł go Singer w 1951 r .
Synonimy :
Stanisław Domański opisywał ten gatunek pod nazwą różanka naga odmiana błękitnosiwa .
Obecną nazwę zaproponował Władysław Wojewoda w 2003 r ..
Morfologia .
Higrofaniczny .
Średnica 4–12 cm , wyjątkowo do 16 cm .
