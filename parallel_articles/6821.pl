Łysiczka łuskowata
Łysiczka łuskowata , pierścieniak łuskowaty ( `` Leratiomyces squamosus `` ( Pers . )
Bridge & amp ; Spooner ) – gatunek grzybów należący do rodziny pierścieniakowatych ( `` Strophariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psilocybe , Strophariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Elias Fries nadając mu nazwę `` Agaricus squamosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2008 r. Paul Dennis Bridge i Brian Martin Spooner , przenosząc go do rodzaju `` Leratiomyces '' .
Niektóre synonimy naukowe :
Ma wiele polskich nazw : pieczarka łuszczkowata , pierścieniak łuskowaty , ostrzępka ceglastawa odmiana łuskowata , pierścieniak łuskowaty .
W 2003 r. Władysław Wojewoda zaproponował jeszcze jedną – łysiczka łuskowata .
Wszystkie nazwy polskie są obecnie niespójne z nazwą naukową , gdyż w 2008 r. gatunek ten przeniesiony został do nowego rodzaju nie mającego polskiej nazwy .
Morfologia .
Cechuje się dość dużą zmiennością morfologiczną .
Dawniej wyróżniano odmiany `` squamosa '' i `` thrausta '' , różniące się barwą i łuskowatością trzonu .
Według Index Fungorum są to jednak synonimy .
