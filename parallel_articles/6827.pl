Les Paul
Les Paul , właśc .
Lester William Polsfuss ( ur .
9 czerwca 1915 w Waukesha , zm .
13 sierpnia 2009 w White Plains ) – amerykański gitarzysta jazzowy i wynalazca w dziedzinie technik nagrywania oraz konstruktor gitar elektrycznych .
Określany jako `` Thomas Edison przemysłu muzycznego '' .
Eksperymentując od końca lat 30. , w 1946 zbudował gitarę elektryczną z litego drewna , bez pudła rezonansowego , opartą na prostej , grubej desce .
Prototyp nie spotkał się z entuzjazmem ze strony producenta gitar , firmy Gibson , lecz Paul został poproszony o konsultacje w sprawie stworzenia nowego modelu gitary tej firmy , nazwanego Gibson Les Paul .
Gitara ta , bardzo ciężka , o charakterystycznym dźwięku , przyczyniła się do powstania muzyki rockowej i pozostaje mniej więcej niezmieniona w produkcji od tego czasu .
Był również wynalazcą i pionierem overdubbingu , delaya oraz metody nagrywania wielośladowego .
Był honorowym członkiem Little Kids Rock , organizacji typu non-profit , zajmującej się dostarczaniem dzieciom w szkołach w Stanach Zjednoczonych darmowych instrumentów muzycznych .
Odznaczony Narodowym Medalem Sztuki .
Życiorys .
Urodził się 9 czerwca 1915 w Waukesha w stanie Wisconsin w rodzinie George ’ a i Evelyn ( z domu Stutz ) Polsfuss .
Zarówno dziadek Lesa Paula ze strony ojca , jak i rodzina matki pochodzili z Niemiec i imigrowali po roku 1870 do Stanów Zjednoczonych .
Rodzice Paula rozwiedli się jeszcze w okresie jego dzieciństwa .
Rodzina zaczęła używać uproszczonej formy nazwiska `` Polfuss '' , nie dokonując jednak oficjalnej zmiany nazwiska .
Lester zaczął używać pseudonimu `` Les Paul '' oraz wymyślonego przez matkę ( od jego rudych włosów ) `` Red Hot Red '' i później `` Rhubarb Red '' .
Les Paul zainteresował się muzyką w wieku ośmiu lat .
Od robotników pracujących w okolicy nauczył się gry na harmonijce ustnej , zafascynował się również pianolą matki .
Dziurkowanie dodatkowych dziurek na kartach do pianoli było pierwszą formą nagrania wielośladowego , które później wynalazł .
Nauczył się gry na banjo , a potem zajął się grą na gitarze .
W wieku trzynastu lat wykonywał półprofesjonalne koncerty muzyki country .
W wieku siedemnastu lat dołączył do Rube Tronson ’ s Texas Cowboys .
