Leslie Nielsen
Leslie William Nielsen ( ur .
11 lutego 1926 w Reginie , zm .
28 listopada 2010 w Fort Lauderdale ) – kanadyjski aktor filmowy i telewizyjny .
Większość życia spędził w Stanach Zjednoczonych i był naturalizowanym Amerykaninem , ale nigdy nie zrzekł się obywatelstwa Kanady , gdzie rozpoczęła się jego kariera aktorska .
Życiorys .
Urodził się w kanadyjskiej Reginie , był trzecim synem Ingvarda Nielsena , oficera kanadyjskiej policji federalnej RCMP – pochodzenia duńskiego i Maybelle – z pochodzenia Walijki .
Jako młodzieniec zgłosił się do Królewskich Sił Lotniczych .
Pracował także jako DJ , zanim otrzymał stypendium od Neighborhood Playhouse w Nowym Jorku , gdzie podjął naukę aktorstwa .
Początkowo występował w rolach przystojnych , „ twardych ” facetów , często grał czarne charaktery .
Debiutował w nieudanym filmie `` The Vagabond King '' , po którym został zaangażowany przez wytwórnię Metro-Goldwyn-Mayer , gdzie wystąpił w filmie fantastycznonaukowym `` Zakazana planeta '' ; w westernie `` Pojedynek w Abilene '' z 1967 zagrał przedsiębiorcę , Granta Eversa .
Największą popularność przyniosły mu jednak role komediowe , szczególnie rola lekarza w `` Czy leci z nami pilot ? ''
