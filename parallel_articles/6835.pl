Lesogorskij
Lesogorskij ( ros .
Лесого́рский , fiń .
`` Jääski '' ) - osiedle typu miejskiego w dystrykcie wyborskim w obwodzie leningradzkim , leżące w Przesmyku Karelskim , w pobliżu granicy rosyjsko-fińskiej .
W 2002 roku 3004 mieszkańców .
Do 1944 roku na terenie Finlandii , centrum administracyjne gminy Jääski .
Stacja kolejowa na linii Kamiennogorsk-Swietogorsk-Imatra .
