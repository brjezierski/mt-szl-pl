Lesotho
Lesotho ( wcześniej Basuto ) – państwo w południowej Afryce , będące enklawą w Republice Południowej Afryki .
Geografia .
Lesotho jest jedynym krajem na świecie położonym w całości na wysokości ponad 1000 m n.p.m .
Wzdłuż granicy południowo-wschodniej rozciąga się pasmo Gór Smoczych ( najwyższy szczyt – Thabana Ntlenyana 3482 m n.p.m .
) , na północy zaś Maloti , z którego wypływa główna rzeka Oranje .
W kierunku zachodnim krajobraz zmienia się z górzystego na wyżynny .
Klimat zwrotnikowy , z temperaturą i opadami zmodyfikowanymi położeniem n.p.m. , w górach mroźne zimy z opadami śniegu , na niżej położonych terenach ciepło i wilgotno .
Naturalną szatę roślinną stanowi sawanna , na stokach gór roślinność trawiasta , w wysokich górach alpejska .
Świat zwierzęcy reprezentują m.in .
: antylopy , zające i różne gatunki gadów .
Historia .
Najstarszymi mieszkańcami współczesnego Lesotho byli Buszmeni .
Ludność buszmeńska przybyła tu prawdopodobnie w I tysiącleciu p.n.e .
Od VIII wieku n.e .
