Leszczydół-Nowiny
Leszczydół-Nowiny – wieś w Polsce położona w województwie mazowieckim , w powiecie wyszkowskim , w gminie Wyszków .
Historia .
W latach 1975–1998 miejscowość administracyjnie należała do województwa ostrołęckiego .
W Leszczydole-Nowinach przyszedł na świat ksiądz Henryk Kietliński ( 1932–2017 ) – pallotyn , długoletni prowincjał polskiej prowincji pallotynów i rektor Wyższego Seminarium Duchownego Stowarzyszenia Apostolstwa Katolickiego w Ołtarzewie , przyjaciel papieża Jana Pawła II .
W czasie II wojny światowej i powstania warszawskiego jeszcze jako dziecko był członkiem Szarych Szeregów i funkcjonował pod pseudonimem `` Dąbek '' .
Instytucje .
W miejscowości znajduje się kościół , który jest siedzibą parafii Chrystusa Sługi .
W strukturze Kościoła rzymskokatolickiego parafia należy do metropolii białostockiej , diecezji łomżyńskiej , dekanatu Wyszków .
Znajduje tu się siedziba Nadleśnictwa Wyszków .
W roku 2004 utworzono Zespół Szkół im .
Jana Pawła II w którego skład wchodziły Szkoła Podstawowa i Gimnazjum .
