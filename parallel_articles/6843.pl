Siedzibą gminy były Leszkowice .
Gmina została utworzona w dniu 20 grudnia 1948 roku w woj .
lubelskim , w powiecie lubartowskim , z obszaru zniesionej gminy Luszawa .
Według stanu z 1 lipca 1952 roku gmina Leszkowice składała się z 11 gromad .
Gmina została zniesiona 29 września 1954 roku wraz z reformą wprowadzającą gromady w miejsce gmin .
Jednostka została przywrócona 1 stycznia 1973 roku po reaktywowaniu gmin w miejsce gromad , lecz pod nazwą `` gmina Ostrówek '' , z siedzibą w Ostrówku .
