Letterkenny
Letterkenny ( irl .
`` Leitir Ceanainn '' ) jest największym miastem w hrabstwie Donegal w Irlandii , mieści się ok. 55 km na północ od Donegal i 32 km na zachód od Derry w Irlandii Północnej .
Jego nazwa jest zangielszczoną nazwą irlandzką , która oznacza `` zbocze rodziny Cannon '' ( `` cannon '' to w języku angielskim armata ) .
Pomimo swojej wielkości Letterkenny nie jest centrum administracji hrabstwa , którego stolicą jest mniejsze Lifford .
