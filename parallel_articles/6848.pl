Czas letni
Czas letni – czas lokalny , który dany kraj przyjmuje dla pewnego okresu roku , zazwyczaj wyprzedzający o godzinę standardowy czas strefowy .
Zmiany czasu mają spowodować efektywniejsze wykorzystanie światła dziennego .
Latem standardowy czas geograficzny jest przesuwany o godzinę do przodu , więc czas aktywności człowieka jest lepiej dopasowany do godzin , w których jest najwięcej światła słonecznego .
Do oznaczania czasu letniego stosowany jest skrót DST pochodzący od ang .
określenia `` Daylight Saving Time '' .
Dla krajów stosujących czas środkowoeuropejski ( CET ) , stosuje się skrót CEST ( ang .
`` Central European Summer Time '' ) .
Historia .
Jako pierwszy o potrzebie stosowania czasu letniego pisał Benjamin Franklin w 1784 .
Jednakże humorystyczna wymowa tego artykułu sprawiła , że nie zaczęto się do niej stosować ( Franklin postulował , by ludzie wstawali i kładli się spać wcześniej ) .
Poważnie temat zmiany czasu opisał Brytyjczyk William Willett w broszurce „ Waste of Daylight ” , wydanej w 1907 r. Jednak pomimo znacznego lobby w rządzie brytyjskim nie udało mu się przeforsować tego pomysłu .
