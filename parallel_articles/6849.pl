Leucoagaricus sericifer
Leucoagaricus sericifer ( Locq . )
Vellinga – gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Leucoagaricus , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1952 r. Marcel Locquin nadając mu nazwę `` Pseudobaeospora sericifera '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Else Vellinga w 2000 r .
Synonimy :
Morfologia .
