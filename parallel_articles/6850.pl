Czubnik czerwieniejący
Czubnik czerwieniejący ( `` Leucoagaricus badhamii '' ( Berk .
& amp ; Broome ) Locq . )
– gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Leucoagaricus , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1854 r. Miles Joseph Berkeley i Christopher Edmund Broome nadając mu nazwę `` Agaricus badhamii '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Rolf Singer w 1951 r .
Synonimy :
Zaleski i in.w 1948 r. opisywali ten gatunek pod nazwą stroszka Badhama .
Władysław Wojewoda w 1998 r. dla naukowej nazwy `` Leucocoprinus badhamii '' zaproponował nową polską nazwę czubnik czerwieniejący .
Nazwa ta stała się niespójna z obecną nazwą naukową
Morfologia .
Duży , o średnicy zazwyczaj do 10 cm , ale czasami nawet do 20 cm , początkowo stożkowy , potem płaskowypukły , w końcu niemal płaski .
Powierzchnia o barwie kremowej , pokryta brązowymi łuskami o fioletowym odcieniu .
