Leuna
Leuna – miasto w Niemczech , w kraju związkowym Saksonia-Anhalt , w powiecie Saale , położone w Saskim Zagłębiu Węglowym , nad Soławą .
31 grudnia 2009 w granicach administracyjnych miasta znalazły się następujące gminy : Friedensdorf , Günthersdorf , Horburg-Maßlau , Kötschlitz , Kötzschau , Kreypau , Rodden , Spergau Zöschen i Zweimen .
Miasto zwiększyło swoją powierzchnię o 73,99 km² , do tego czasu należało także do wspólnoty administracyjnej Leuna-Kötzschau .
