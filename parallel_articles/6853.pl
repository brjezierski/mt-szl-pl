Levallois-Perret
Levallois-Perret – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Levallois-Perret jest miastem o największej gęstości zaludnienia w całej Francji .
Wg danych na rok 2012 gminę zamieszkiwało 64 654 osób , a gęstość zaludnienia wynosiła 26 827 osób/km² .
Wśród 1287 gmin regionu Île-de-France Levallois-Perret plasuje się na miejscu 848. pod względem powierzchni .
W Levallois-Perret wytworzono części metalowe wieży Eiffla przed zmontowaniem w Paryżu , a także strukturę metalową dla Statui Wolności .
