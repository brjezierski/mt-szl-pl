Leveillula taurica
Leveillula taurica ( Lév . )
G. Arnaud – gatunek grzybów należący do rzędu rodziny mączniakowatych ( `` Erysiphaceae '' ) ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Leveillula , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1851 roku Joseph Henri Léveillé nadając mu nazwę `` Erysiphe taurica '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1921 roku Gabriel Arnaud , przenosząc go do rodzaju `` Leveillula '' .
