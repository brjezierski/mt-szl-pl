Lewocza
Lewocza ( słow .
`` Levoča '' , węg .
`` Lőcse '' , niem .
`` Leutschau '' , łac .
`` Leutsovia '' ) – miasto powiatowe we wschodniej Słowacji , w kraju preszowskim , w historycznym regionie Spisz .
Miasto leży na wysokości 570 m n.p.m .
w Kotlinie Hornadzkiej , u południowych podnóży Gór Lewockich , nad rzeczką `` Levočsky potok '' .
Liczba mieszkańców miasta wynosi 14 830 osób ( spis ludności z 21 maja 2011 ) , powierzchnia miasta – 114,767 km² .
Dzieli się na dzielnice :
Lewocza leży przy głównej słowackiej drodze krajowej nr 18 ( D1 , międzynarodowej E50 ) , między Popradem a Preszowem .
W Lewoczy kończy się odnoga linii kolejowej z Koszyc i Preszowa do Popradu , odbijająca od tej linii w Spiskiej Nowej Wsi .
