Lewes
Lewes – miasto i civil parish w Anglii , ośrodek administracyjny hrabstwa East Sussex i dystryktu Lewes .
W 2011 roku civil parish liczyła 17 297 mieszkańców .
Położone nad rzeką Ouse .
Miasto o bogatej historii , sięgającej początków państwowości Anglii .
Dominującą nad miastem budowlą jest zamek .
Miasto posiada własny środek płatniczy , jakim jest funt miasta Lewes .
Lewes jest wspomniane w Domesday Book ( 1086 ) jako `` Lewes '' / '' Lau ( u ) es '' .
