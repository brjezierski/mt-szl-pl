Lewis Capaldi
Lewis Capaldi ( ur .
7 października 1996 r. w Glasgow ) – szkocki piosenkarz i autor tekstów .
W październiku 2017 roku Capaldi wypuścił na rynek swój debiutancki minialbum `` Bloom '' , zawierający utwory „ Bruises ” , „ Lost On You ” , czy „ Fade ” .
W listopadzie 2018 r. swoją premierę miała EP-ka `` Breach '' .
Pochodzący z niej singel „ Someone You Loved ” osiągnął sukces komercyjny docierając na szczyt listy UK Singles Chart , gdzie pozostał tam przez siedem tygodni oraz prestiżowego notowania `` Billboard '' Hot 100 .
To nagranie było również nominowane w kategorii „ Piosenka roku ” podczas 62. ceremonii wręczenia nagród Grammy .
W Polsce „ Someone You Loved ” uzyskało certyfikat diamentowej płyty .
17 maja 2019 miał premierę debiutancki album studyjny Capaldiego , `` Divinely Uninspired to a Hellish Extent '' .
Osiągnął pierwsze miejsce najlepiej sprzedających się płyt w Wielkiej Brytanii .
W Polsce w październiku 2022 nagrania uzyskały certyfikat platynowej płyty .
