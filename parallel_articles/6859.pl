Lewis Hamilton
Sir Lewis Carl Davidson Hamilton MBE ( ur .
7 stycznia 1985 w Stevenage ) – brytyjski kierowca wyścigowy , siedmiokrotny mistrz świata Formuły 1 w sezonach 2008 , 2014 , 2015 , 2017 , 2018 , 2019 , 2020 , a także wicemistrz tej serii w sezonach 2007 , 2016 , 2021 .
Życiorys .
Początki kariery .
Po zwycięstwach w kilku mistrzostwach kartingowych , w wieku 12 lat został przyjęty do programu rozwojowego młodych kierowców McLarena .
Wcześniej brał udział w Brytyjskiej Formule Renault , w 2000 roku był także europejskim mistrzem w kartingu .
W 2005 roku zwyciężył w serii Formuły 3 Euro Series , jeżdżąc dla zespołu ASM .
Rok wcześniej reprezentował barwy Manor Motorsport .
