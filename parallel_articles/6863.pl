Język lezgiński
Język lezgiński ( także : język lezgijski ; lezg .
, ) – język Lezginów , zamieszkujących południowo-wschodnią część Dagestanu i północne rejony Azerbejdżanu .
Należy do zespołu samurskiego , podgrupy dagestańskiej w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Według danych spisu powszechnego przeprowadzonego w 2002 r. językiem tym na terenie Federacji Rosyjskiej włada ponad 385 tys .
osób .
Podczas spisu ludności przeprowadzonego w 1997 r. w Azerbejdżanie 197 tys .
osób podało narodowość lezgińską ( brak danych o języku ojczystym ) .
Wiele szacunków i danych liczbowych odnosi się nie do języka lezgińskiego jako takiego , lecz do całej grupy spokrewnionych języków kaukaskich , włącznie z językiem tabasarańskim , agulskim , rutulskim , cachurskim i kilkoma drobniejszymi językami regionu .
