Wólka Leśna
Wólka Leśna – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 12 km na północ od Siedlec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Mikołaja w Pruszynie .
Przez wieś przebiega droga powiatowa do Pruszyna .
