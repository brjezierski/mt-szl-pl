Leonowo ( Białoruś )
Leonowo – część wsi Mamiany na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Widze .
Dawniej folwark .
Historia .
W dwudziestoleciu międzywojennym folwark leżał w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie brasławskim , w gminie Dryświaty .
Według Powszechnego Spisu Ludności z 1921 roku folwark zamieszkiwało 3 osoby , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Był tu 1 budynek mieszkalny .
W 1938 zamieszkiwało tu 6 osób w 2 budynkach .
Miejscowość należała do parafii rzymskokatolickiej w Dryświatach .
Podlegała pod Sąd Grodzki w m. Turmont i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Dyrświatach .
