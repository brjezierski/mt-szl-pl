Leśnica ( Białoruś )
Leśnica ( biał .
`` Лясніца '' , ros .
`` Лесница '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Hoża , nad rzeką o tej samej nazwie .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Hoża .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 165 osób , 164 było wyznania rzymskokatolickiego a 1 prawosławnego .
Jednocześnie wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Było tu 29 budynków mieszkalnych .
Miejscowość należała do parafii świętych apostołów Piotra i Pawła w Hoży .
Wieś podlegała pod Sąd Grodzki i Okręgowy w Grodnie , najbliższy urząd pocztowy mieścił się w Hoży .
W pobliżu wsi istniały dwie osady młyńskie – Leśnica I i Leśnica II .
