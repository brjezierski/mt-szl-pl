Leżajsk
Leżajsk – miasto w Polsce , w północnej części województwa podkarpackiego , siedziba powiatu leżajskiego .
Leży w dolinie Sanu , na skraju dawnej Puszczy Sandomierskiej , w dawnej ziemi przemyskiej .
Według danych z 1 stycznia 2018 Leżajsk liczył 13 990 mieszkańców .
Położenie .
Według danych z 1 stycznia 2011 r. powierzchnia miasta wynosiła 20,58 km² .
