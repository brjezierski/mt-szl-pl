Należał do bocznej linii rodu cesarskiego .
Miejsce urodzenia jest niepewne .
Wiadomo , że gdy miał pięć lat , jego rodzina przeniosła się do Jiangyou , w pobliżu obecnego Chengdu w Syczuanie .
Formalną edukację rozpoczął w wieku 10 lat .
W 720 roku , gdy miał 18 lat , przepytał go gubernator Su Ting , który orzekł , że ma do czynienia z geniuszem .
Choć wyraził chęć zostania urzędnikiem , nie zadał sobie trudu , aby stawić się na obowiązkowy egzamin .
Zamiast tego , w wieku 25 lat rozpoczął podróże po kraju .
