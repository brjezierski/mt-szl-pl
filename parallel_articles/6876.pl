Li Bai
Li Bo / Li Bai ( 李白 ) , znany też jako Li Taibo / Li Taibai ( 李太白 ) ; ur .
701 , zm .
762 – chiński poeta z okresu `` złotego wieku '' chińskiej poezji .
Obok swojego przyjaciela Du Fu często wymieniany jako jeden z dwóch największych poetów w dziejach literatury chińskiej .
Zachowało się ponad 1000 jego utworów .
W Europie poezja Li Bai pojawiła się dzięki jezuickiemu misjonarzowi w Pekinie Jean Joseph Marie Amiot , który przedstawił ją w swoich `` Portraits des Célèbres Chinois '' , wydanych w serii `` Mémoires concernant l ’ histoire , les sciences , les arts , les mœurs , les usages , & amp ; c. des Chinois , par les missionnaires de Pekin '' .
( 1776–1797 ) .
Kolejne tłumaczenie na francuski pojawiło się w roku 1862 , w jego `` Poésies de l ’ Époque des Thang '' markiza d ’ Hervey de Saint-Denys .
Na jego recepcję w świecie anglojęzycznym największy wpływ miały swobodne , ale poetycko ważne tłumaczenia japońskich wersji jego wierszy dokonane przez Ezrę Pounda .
Li Bai zasłynął najbardziej ze swej bujnej wyobraźni poetyckiej i z elementów taoistycznych obecnych w jego poezji , a także z zamiłowania do mocnych trunków .
Podobnie jak Du Fu spędził znaczną część życia , podróżując , choć w jego wypadku działo się tak dlatego , że pozwalała mu na to jego zamożność , a nie dlatego , że zmuszała go do tego bieda .
Od stuleci utrzymuje się na jego temat legenda , jakoby utopił się , próbując po pijanemu przepić do odbicia księżyca w rzece Jangcy , choć najprawdopodobniej nie jest to prawdą .
