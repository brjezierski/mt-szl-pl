Informacje podstawowe .
Stanowiska państwowe i rządowe obsadzane są według klucza wyznaniowego : prezydent – chrześcijanin maronita , premier – muzułmanin sunnita , przewodniczący parlamentu – muzułmanin szyita , równy podział tek ministerialnych , wyższych stanowisk w administracji państwowej oraz miejsc w parlamencie między przedstawicieli ludności muzułmańskiej i chrześcijańskiej .
Pochodzenie nazwy .
Nazwa Liban ( w standardowym języku arabskim „ Lubnān ” ; „ Lebnan ” albo „ Lebnèn ” w lokalnym dialekcie ) ma swoje źródło w semickim „ LBN ” , które łączy się z kilkoma słowami bliskoznacznymi w różnych językach , takimi jak `` biały '' czy `` mleko '' .
Stanowi to odniesienie do pokrytych śniegiem Gór Libanu .
Takie określenia zostały znalezione w Biblii ; słowo „ Liban ” jest wspomniane 71 razy w Starym Testamencie .
Znaleziono je też na 3 z 12 tablic Eposu o Gilgameszu , w tekście znalezionym w ruinach starożytnego miasta Ebla .
