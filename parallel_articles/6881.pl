Liberec
Liberec ( , cyg .
`` Libercys '' ) – miasto w północnych Czechach nad Nysą Łużycką , stolica kraju libereckiego , ośrodek sportów zimowych położony u podnóży Ještěda .
W 1577 roku miejscowość otrzymała prawa miejskie .
Po I wojnie światowej była to stolica prowincji Niemieckie Czechy ( , ) .
Do końca II wojny światowej zamieszkiwany był głównie przez Niemców sudeckich .
W czasie II wojny Liberec był stolicą Okręgu III Rzeszy – Kraju Sudetów .
Położenie .
Miasto leży w północnych Czechach , w Sudetach Zachodnich , na terenie Kotliny Żytawskiej ( ; przedłużeniem tej kotliny po stronie polskiej jest Obniżenie Żytawsko-Zgorzeleckie ) , w Kotlinie Libereckiej ( ) , pomiędzy Górami Izerskimi na północy a Grzbietem Jesztiedzkim na południu .
Ratusz leży na wysokości 374 m n.p.m .
Najniższy punkt w granicach miasta znajduje się w części zwanej `` Machnín '' – 325 m n.p.m. , a najwyższy na szczycie Ještědu – 1012 m n.p.m .
Przez miasto przepływa Nysa Łużycka oraz jej dopływy – Černá Nisa i Harcovský potok .
Rekreacja i wypoczynek .
Liberec jest dość chętnie odwiedzanym miastem przez osoby poszukujące aktywnego wypoczynku .
Oprócz kompleksu narciarskiego w mieście istnieją dwa aquaparki – `` Plavecký bazén Liberec '' i Centrum Babylon Liberec ( część kompleksu hotelowego i rozrywkowego ) oraz jedno odkryte kąpielisko `` Koupaliště Vápenka '' .
W mieście znajduje się ogród zoologiczny oraz ogród botaniczny
Sport .
Miasto było organizatorem Mistrzostw Świata w Narciarstwie Klasycznym 2009 .
