Liberia
Liberia ( Republika Liberii – `` Republic of Liberia '' ) – państwo położone w Afryce Zachodniej .
Sąsiaduje z Gwineą , Wybrzeżem Kości Słoniowej oraz Sierra Leone .
Pod koniec lat 80 .
XX wieku sytuacja gospodarcza i polityczna tego kraju stała się niestabilna , co było skutkiem przewrotów wojskowych i dwóch wojen domowych ( 1989–1996 i 1999–2003 ) .
Członek Unii Afrykańskiej i ECOWAS .
Geografia .
Państwo położone w Afryce Zachodniej kilkaset kilometrów na północ od równika .
Połowa powierzchni pokryta jest lasem tropikalnym .
Ukształtowanie terenu zmienia się wraz z oddaleniem od wybrzeża – początkowa równina przechodzi stopniowo w pofałdowany płaskowyż , a następnie w niewysokie góry w północno-wschodniej części kraju .
Średnia temperatura utrzymuje się na poziomie 21 stopni Celsjusza .
Maksimum to 37 °C .
Stosunkowo niewielkie są wahania temperatury spowodowane zmianą pór dnia i roku .
Występują dwie pory roku : sucha i deszczowa .
Historia .
Jest to najstarsza republika w Afryce .
Powstała w wyniku umowy między Amerykańskim Towarzystwem Kolonizacyjnym a autochtonami , podpisanej w 1821 roku .
Dzięki niej wyzwoleni niewolnicy ze Stanów Zjednoczonych mieli osiedlić się w tej części kontynentu , na wykupionych przez Towarzystwo terenach , i żyć w zgodzie z 18 plemionami tubylczymi .
Początkowo Liberia była zależna od Stanów Zjednoczonych .
26 lipca 1847 roku Amerykano-Liberyjczycy uchwalili konstytucję , jednocześnie deklarując własną niepodległość .
Nazwa kraju pochodzi od łacińskiego słowa `` liber '' – „ wolny ” .
Historia Liberii zdominowana była przez konflikty pomiędzy kolonią wyzwolonych niewolników , a rdzenną ludnością ( zapoczątkowane rozszerzaniem terytorium kolonii poza wykupione wcześniej ziemie ) oraz przez spory terytorialne z sąsiadującymi koloniami europejskimi .
Do 1945 rdzenna ludność nie posiadała reprezentacji w parlamencie .
Prawo do głosowania – ograniczone cenzusem majątkowym – uzyskała w 1947 roku , za rządów prezydenta Williama Tubmana , który dążył do zmniejszenia nierówności społecznej między potomkami wyzwoleńców , a rdzenną ludnością .
Dominacja Amerykano-Liberyjczyków zakończyła się w 1980 roku .
W 1980 Samuel K. Doe przeprowadził przewrót wojskowy , w którym zabito urzędującego prezydenta Williama Tolberta , a władzę przejęła Ludowa Rada Ocalenia ( pod przywództwem S.K .
Doe ) .
