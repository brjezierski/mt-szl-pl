Libiąż ( stacja kolejowa )
Libiąż – stacja kolejowa w Libiążu , w województwie małopolskim , w Polsce .
Linia kolejowa przebiegająca przez Libiąż została zbudowana jako kolej ze Lwowa do Wiednia .
Otwarcie jej nastąpiło 1 lutego 1856 .
Na początku była to linia jednotorowa , o rozstawie szyn 1450 mm .
Na stacji wybudowano budynek stacyjny , posiadający pomieszczenia dyżurnego ruchu , poczekalnię .
W roku 2021 stacja obsługiwała 50–99 pasażerów na dobę .
Stacja w Libiążu oprócz obsługi pociągów osobowych , ze względu na istniejącą kopalnię węgla kamiennego , obsługiwała ruch pociągów towarowych .
Historia .
W związku ze zwiększonym zapotrzebowaniem na przewozy kolejowe w latach 60. i 70 .
XX wieku , podjęto decyzję o elektryfikacji tej linii .
Pierwszy pociąg elektryczny przejechał 30 kwietnia 1973 roku .
