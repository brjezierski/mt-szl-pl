Historia .
Okolica była początkowo zamieszkana przez plemię Mpongwe długo przed przybyciem Francuzów , którzy nabyli tę ziemię w 1839 roku .
Miasto zostało założone w 1843 roku .
W 1848 roku miasto zostało nazwane Libreville ( z fr .
`` wolne miasto '' ) .
Miasto w latach 1934-1946 było głównym portem Francuskiej Afryki Równikowej .
Po uzyskaniu w 1960 roku niepodległości , miasto zamieszkiwało 31 000 ludzi .
