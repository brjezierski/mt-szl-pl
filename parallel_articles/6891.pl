Lichynia
Lichynia ( ) – wieś w Polsce położona w województwie opolskim , w powiecie strzeleckim , w gminie Leśnica .
W latach 1975–1998 miejscowość położona była w województwie opolskim .
Wieś Lichynia leży na wschód od Leśnicy .
Historia .
Po raz pierwszy Lichynia wzmiankowana jest w 1223 roku jako „ Lichan ” .
W 1410 roku książę opolski Bolko IV prowadził spór ze swoim bratem Bernarden o zboże z Lichyni , Zalesia Śląskiego i Leśnicy Opolskiej .
Chłopi z Lichyni byli zobowiązani w tych czasach w ramach pańszczyzny do robocizny na rzecz folwarku w Zalesiu W 1455 roku wieś została wymieniona jako „ Lichinya ” , a w 1534 roku jako „ Lichina ” .
Późniejsze dzieje Lichyni związane są z pobliskimi Sławięcicami .
Spis dóbr sławięcickich z 1576 roku wymieniał wioskę „ Lichinię ” .
Wspomniane dobra wielokrotnie zmieniały właścicieli .
W 1782 roku Lichynia przeszła w ręce rodziny Hohenlohe , która władała na tym terenie do 1945 roku .
