Liechtenstein
Liechtenstein ( [ ] , ) , Księstwo Liechtensteinu ( niem .
, ) – państwo unitarne położone w Europie Zachodniej , w dolinie górnego Renu , w Alpach Retyckich , między Austrią a Szwajcarią .
Powierzchnia Liechtensteinu wynosi 160,44 km² , co daje mu 189. miejsce na świecie , a zamieszkiwane jest przez 39 711 osób , co z kolei daje 187. miejsce na świecie .
Liechtenstein składa się z jedenastu gmin , posiadających szeroką autonomię , ale pozostających pod zwierzchnictwem władzy centralnej .
Siedzibą władz państwowych i konstytucyjną stolicą państwa jest Vaduz .
Państwo jest monarchią konstytucyjną , na której czele stoi Książę Liechtensteinu .
Obecnie panującym księciem jest Jan Adam II .
Podstawą ustroju Liechtensteinu jest konstytucja z 1921 r. Władzę ustawodawczą sprawuje jednoizbowy ( unikameralizm ) parlament – Landtag .
Księstwo Liechtensteinu powstało w 1719 r. z połączenia majątków Schellenberg oraz Vaduz i od tamtej pory państwem rządzi nim dynastia Liechtensteinów .
Było częścią Świętego Cesarstwa Rzymskiego , Związku Reńskiego i ostatecznie Związku Niemieckiego , a po jego rozwiązaniu w 1866 r. stało się suwerennym państwem .
Podczas obu wojen światowych Liechtenstein pozostał neutralny , a od lat 50 .
XX wieku doszło w kraju do ożywienia gospodarczego i napływu zagranicznego kapitału , w wyniku czego państwo stało się najbogatszym lub jednym z najbogatszych państw świata z PKB per capita równym 169,3 tys .
dolarów amerykańskich .
Liechtenstein znajduje się w unii celnej i monetarnej ze Szwajcarią , należy do Organizacji Narodów Zjednoczonych , Rady Europy , Europejskiego Obszaru Gospodarczego , Europejskiego Stowarzyszenia Wolnego Handlu , Światowej Organizacji Handlu i strefy Schengen .
Geografia .
Liechtenstein położony jest nad górnym Renem , w Alpach .
Cała granica zachodnia państwa przebiega wzdłuż biegu Renu , wschodnia zaś wyznaczana jest przez łańcuchy alpejskie .
Najwyższym szczytem Liechtensteinu jest Grauspitz o wysokości 2599 m n.p.m .
Powierzchnia Liechtensteinu zwiększyła się o około 0,5 km² ( kosztem Austrii ) , a granica wydłużyła o 1,9 km , gdy 28 grudnia 2006 roku rząd dokładnie określił granicę państwa .
Historia .
Obecne tereny Liechtensteinu już ponad 1500 lat temu zostały zasiedlone przez Alamanów i innych Germanów .
W tym czasie Karol Wielki włączył te ziemie do swego imperium .
Przez wieki te tereny były podzielone na dwa hrabstwa : jedno ze stolicą w Vaduz , a drugie w Schellenbergu .
Hrabstwo Schellenberg dzieliło losy ważniejszego hrabstwa Vaduz .
Na przełomie XVII i XVIII wieku obydwa hrabstwa zostały zakupione przez ród Liechtensteinów .
W 1719 roku ziemie te zostały uznane przez cesarza za suwerenne księstwo w granicach Rzeszy Niemieckiej .
W roku 1806 cesarz Świętego Cesarstwa Rzymskiego Franciszek II ( I ) Habsburg został zmuszony przez Napoleona do abdykacji i zrzeczenia się tytułu cesarza rzymskiego .
Liechtenstein w 1806 został członkiem Związku Reńskiego , który miał zastąpić cesarstwo i pozostał w nim do 1814 roku .
W latach 1815–1866 był członkiem Związku Niemieckiego .
Przez te wszystkie lata księstwo utrzymywało ścisłe kontakty gospodarcze i polityczne z Austrią .
Wynikiem tego było w 1866 wystąpienie z nią ze Związku Niemieckiego i uzyskanie przez Liechtenstein pełnej niepodległości .
Podczas I wojny światowej państwo było neutralne .
Po wojnie więzy z Austrią zostały zastąpione przez umowy ze Szwajcarią .
24 października 1921 w Liechtensteinie ustanowiono konstytucję , według której kraj jest monarchią o demokratyczno-parlamentarnej formie rządów .
W 1924 państwo weszło w skład szwajcarskiego związku celnego .
