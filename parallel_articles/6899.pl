Lietuvos nacionalinis radijas ir televizija
Lietuvos nacionalinis radijas ir televizija ( LRT ) – litewski publiczny nadawca radiowo-telewizyjny z siedzibą w Wilnie .
Jako rozgłośnia radiowa istnieje od 1926 , telewizja została uruchomiona w 1957 roku .
Obecnie LRT nadaje trzy programy radiowe i trzy programy telewizyjne .
Na Litwie nie istnieje obecnie abonament radiowo-telewizyjny , choć jego wprowadzenie jest rozważane od roku 1996 .
Na budżet LRT składają się dotacje bezpośrednio z budżetu państwa ( 75 % ) oraz dochody z działalności komercyjnej , przede wszystkim nadawania reklam ( 25 % ) .
Firma zatrudnia około 650 osób .
