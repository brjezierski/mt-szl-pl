Faza grupowa .
Losowanie odbyło się 29 sierpnia 2019 roku w Monako .
Do startu w fazie grupowej uprawnione były 32 drużyny ( w tym 6 zwycięzców rundy play-off oraz zwycięzca Ligi Europy z poprzedniego sezonu ) .
W trakcie losowania zespoły zostały rozdzielone na 4 koszyki , według współczynnika UEFA , a następnie rozlosowane i podzielone na 8 grup po 4 drużyny każda .
Do jednej grupy nie będą mogły trafić drużyny z tego samego koszyka i federacji oraz decyzją UEFA drużyny z Rosji i Ukrainy .
Wszystkie zespoły zagrały ze sobą dwukrotnie – po 2 najlepsze z każdej grupy awansowały do fazy pucharowej , drużyny z 3. miejsc otrzymały prawo gry w 1/16 finału Ligi Europy UEFA .
