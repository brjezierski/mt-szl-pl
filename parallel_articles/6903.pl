Kuźnica Ligocka
Kuźnica Ligocka ( niem .
`` Ellguth-Hammer '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
Miejscowość leży na południowym krańcu Borów Niemodlińskich ( pozostałość dawnej Puszczy Niemodlińskiej ) , na ziemi prudnickiej .
Przez miejscowość przepływa rzeka Ścinawa Niemodlińska .
