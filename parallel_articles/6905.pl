Legnica ( stacja kolejowa )
Legnica – główna osobowa węzłowa stacja kolejowa w Legnicy , położona na linii nr 275 z Wrocławia do Gubinka , stanowiąca jednocześnie początek linii do Katowic , Jerzmanic-Zdroju i Rudnej .
Została ona otwarta 18 października 1844 .
Zatrzymują się na niej wszystkie rodzaje przejeżdżających pociągów .
Według klasyfikacji PKP ma kategorię dworca wojewódzkiego .
