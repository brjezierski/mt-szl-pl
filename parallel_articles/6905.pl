Położenie .
Stacja położona jest w centralnej części Legnicy , między ulicą Dworcową i Ścinawską w sąsiedztwie legnickiego Starego Miasta , około kilometr od Rynku .
Administracyjnie leży w województwie dolnośląskim , w mieście na prawach powiatu Legnica .
Stacja znajduje się na wysokości 115 m n.p.m .
