Jaśkowice Legnickie ( przystanek kolejowy )
Jaśkowice Legnickie – osobowy przystanek kolejowy ( dawniej stacja ) w Jaśkowicach Legnickich , w województwie dolnośląskim , w Polsce .
Położony na linii nr 275 z Wrocławia do Gubina .
W latach 2009–2010 przebudowany w ramach modernizacji magistrali kolejowej E30 na odcinku Legnica – Wrocław .
W roku 2017 przystanek obsługiwał 200–299 pasażerów na dobę
