Lil Loaded
Lil Loaded , właśc .
Dashawn Maurice Robertson ( ur .
1 sierpnia 2000 w San Bernadino , zm .
31 maja 2021 w Cedar Hill ) – amerykański raper , piosenkarz i autor tekstów .
Życiorys .
Karierę rapera rozpoczął pod koniec 2018 r. Szerszy rozgłos zdobył wtedy , gdy jego piosenka „ 6locc 6a6y ” zainteresowała YouTubera Tommy'ego Craze ’ a .
Pomysł Craze ’ a polegał na tym , że poddawał ocenie teledyski , które nie cieszyły się zainteresowaniem odbiorczym .
Pozytywna opinia Craze ' a spowodowała , że teledysk osiągnął ponad 28 mln wyświetleń w serwisie YouTube , a następnie uzyskał status złotej płyty .
Kolejnym sukcesem Robertsona stał się utwór „ Gang Unit ” , który został odtworzony ponad 39 mln razy .
Po wydaniu „ 6locc 6a6y ” raper podpisał kontrakt z Epic Records .
Następnie wydał mixtape CRIPTAPE i album `` A Demon In 6lue '' .
Problemy z prawem .
25 października 2020 r. w trakcie nagrywania teledysku postrzelił ze skutkiem śmiertelnym 18-letniego Khalia Walkera .
9 listopada 2020 r. dobrowolnie zgłosił się na policję , a w lutym 2021 r. oskarżono go o nieumyślne spowodowanie śmierci .
Śmierć .
Latem 2020 r. , przebywając w szpitalu , poinformował za pośrednictwem mediów społecznościowych , że został postrzelony , jednakże szybko powrócił do zdrowia .
31 maja 2021 r. popełnił samobójstwo .
