Żylak liliowy
Żylak liliowy ( `` Phlebia lilascens '' ( Bourdot ) J. Erikss .
& amp ; Hjortstam ) – gatunek grzybów z rodziny strocznikowatych ( `` Meruliaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Phlebia , Meruliaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1910 r. Hubert Bourdot nadając mu nazwę `` Corticium lilascens '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Ove Erik Eriksson i Kurt Hjortstam w 1981 r .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Jednoroczny , rozpostarty , ściśle przylegający do podłoża , o grubości 0,1–0,4 mm .
W stanie świeżym ma woskowatą konsystencję , po wyschnięciu skorupiastą .
