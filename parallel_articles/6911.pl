Lilith ( film 1964 )
Lilith – amerykański film w reżyserii Roberta Rossena z 1964 roku .
W rolach głównych wystąpili Warren Beatty i Jean Seberg .
Film opowiada o schizofreni , miłości i zagubieniu w świecie .
Autorem powieści , pod tym samym tytułem , na podstawie której został nakręcony obraz , był J. R. Salamanca .
`` Lilith '' był ostatnim filmem Roberta Rossena .
Fabuła .
Vincent Bruce ( Warren Beatty ) , młody weteran wojny koreańskiej , wrócił do swojej miejscowości , gdzie zaczął pracować jako terapeuta w zakładzie psychiatrycznym .
Spotyka tam pacjentkę , piękną i tajemniczą Lilith Arthur ( Jean Seberg ) .
Początkowo Vincent robi wszystko , by zachować z nią profesjonalne relacje , niemniej sytuacja wymyka się spod kontroli .
