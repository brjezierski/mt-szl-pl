Lillehammer
Lillehammer – norweskie miasto i gmina leżąca w okręgu Innlandet , nad jeziorem Mjøsa .
Lillehammer jest 211. norweską gminą pod względem powierzchni .
Znajdują się tam skocznie narciarskie Lysgårdsbakken .
W 1994 roku odbyły się tu XVII Zimowe Igrzyska Olimpijskie .
Demografia .
Według danych z roku 2005 gminę zamieszkuje 25 075 osób .
Gęstość zaludnienia wynosi 52,57 os./km² .
Pod względem zaludnienia Lillehammer zajmuje 33. miejsce wśród norweskich gmin .
