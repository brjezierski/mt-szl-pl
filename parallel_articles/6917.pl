Muchomornica delikatna
Muchomornica delikatna ( `` Limacella delicata `` ( Fr . )
Earle ex Konrad & amp ; Maubl . )
– gatunek grzybów z rodziny muchomorowatych ( `` Amanitaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Amanitaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' według Index Fungorum .
Po raz pierwszy takson ten zdiagnozował w roku 1821 Elias Fries nadając mu nazwę `` Agaricus delicatus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w roku 1930 Franklin Sumner Earle ex Paul Konrad i André Maublanc , przenosząc go do rodzaju `` Limacella '' .
Synonimów ma ponad 20 .
Niektóre z nich :
Nazwę polską zaproponował Władysław Wojewoda w 2003 roku , Stanisław Chełchowski w 1898 r. opisywał go jako bedłka delikatna .
Czasami opisywana jest też jako muchomornica kroplista .
Morfologia .
Średnica 3–6 cm , kształt u młodych owocników stożkowaty lub dzwonkowaty , później łukowaty i łukowaty , w końcu rozpostarty .
Brzeg długo podwinięty , czasami ze zwieszającymi się resztkami osłony .
