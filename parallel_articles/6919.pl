Limassol
Limassol ( gr .
`` Λεμεσός '' trl .
`` Lemesós '' ; tur .
`` Limasol '' lub `` Leymosun '' ) – drugie co do wielkości miasto Cypru , położone na południowym wybrzeżu wyspy nad Morzem Śródziemnym , u podnóża Gór Trodos .
Stanowi ośrodek administracyjny dystryktu Limassol .
W 2011 roku liczyło 101 000 mieszkańców , natomiast w całej aglomeracji 237 000 mieszkańców .
Sąsiaduje z brytyjskim terytorium zamorskim i bazą Akrotiri .
Po roku 1974 , kiedy północny Cypr zajęty został przez wojska tureckie , miasto uległo olbrzymim przeobrażeniom , stając się największym portem tranzytowym na Cyprze .
Stało się ono znaczącym ośrodkiem turystycznym .
O jego walorach zadecydowała długa tradycja kulturowa .
Na jego terenie znajduje się kilka muzeów i stanowisk archeologicznych o europejskim znaczeniu .
