Limbang
Limbang – miasto i jeden z 11 dystryktów , na które podzielony jest malezyjski stan Sarawak .
Dystrykt ma powierzchnię 3978 km² , według danych ze spisu ludności z 2000 roku zamieszkiwało go 42 600 mieszkańców .
Teren ten został anektowany przez Jamesa Brooke i wcielony do Sarawaku w 1890 , poprzednio należał do Brunei .
Sułtanat Brunei do dziś kwestionuje legalność tej aneksji i wysuwa roszczenia o zwrot dystryktu pod adresem Malezji .
Stolica dystryktu leży nad rzeką o tej samej nazwie .
Dystrykt Limbang dzieli terytorium Brunei na dwie części – wschodnią i zachodnią .
