Limerick ( hrabstwo )
Limerick ( irl .
`` Contae Luimnigh '' ) – irlandzkie hrabstwo w prowincji Munster , leżące na środkowym zachodzie wyspy .
Sąsiaduje z hrabstwami : Clare na północy , Cork na południu , Tipperary na wschodzie oraz Kerry na zachodzie .
Hrabstwo posiada dużą liczbę zamków .
Nazwa `` Luimneach '' ( oznaczająca `` pokryty płaszczami , pelerynami '' ) wzięła się od pływających w rzece Shannon płaszczy po bitwie .
Geografia .
Przez hrabstwo przepływa rzeka Shannon , która płynie przez miasto Limerick i wpada do Atlantyku .
Ponieważ estuarium jest płytkie , najbardziej znaczący port hrabstwa położony jest kilkanaście kilometrów na zachód od miasta Limerick – w Foynes .
Znaczącym miastem poza Limerick jest Newcastlewest .
Sport .
Popularnym sportem w hrabstwie jest rugby .
Hrabstwo wygrało krajowe zawody hurlingowe siedem razy , ostatnio w roku 1973 .
