Linköping
Linköping – miasto w Szwecji , stolica regionu administracyjnego Östergötland , nad rzeką Stångån .
Miasto liczy 113 042 ( 2018 ) mieszkańców , a gmina Linköping , około 149 906 ( 2013 ) .
Linköping jest siódmym co do wielkości miastem w Szwecji .
Linköping to także siedziba diecezji Linköping ( kościół szwedzki ) .
