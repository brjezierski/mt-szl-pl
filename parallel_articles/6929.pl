Linus Torvalds
( ur .
28 grudnia 1969 w Helsinkach ) – fiński programista , twórca jądra Linux oraz systemu kontroli wersji Git .
Należy do społeczności Finów szwedzkojęzycznych .
Uczęszczał na Uniwersytet Helsiński .
Poprzez stworzenie jądra Linux nadał impuls do gwałtownego rozwoju wolnego oprogramowania .
Sam należy do zwolenników powstałego później ruchu otwartego oprogramowania .
W 2000 roku otrzymał od Steve ’ a Jobsa propozycję pracy w Apple Inc. oraz współpracy nad powstającym wtedy Mac OS X. Linus odrzucił ją , twierdząc , że „ nienawidzi mikrojądra Mach ” .
Jest synem dziennikarza i polityka Nilsa Torvaldsa .
Mieszka w Portland w stanie Oregon ( Stany Zjednoczone ) , z żoną Tove ( byłą mistrzynią Finlandii w karate ) i trzema córkami : Patricią , Mirandą i Danielą .
Jest zatrudniony w Linux Foundation ( dawniej Open Source Development Labs ) .
