Linz
Linz ( ) – miasto statutarne w północno-wschodniej Austrii , siedziba kraju związkowego Górna Austria oraz powiatów : Linz-Land oraz Urfahr-Umgebung , do których miasto jednak nie należy .
Leży nad Dunajem , założone przez Rzymian pod nazwą `` Lenthia '' .
5 marca 1684 powołano w Linzu antyturecką Ligę Świętą .
Współcześnie rozwinięty przemysł metalowy ( huta żelaza ) , maszynowy i chemiczny .
Ważny węzeł komunikacyjny .
Ośrodek turystyczny ( m.in .
zamek z XVI-XVII w. , barokowe pałace i kościoły , liczne muzea ) .
Liczy 197 283 mieszkańców ( 1 stycznia 2015 ) .
47,7 % powierzchni miasta przypada na tereny zielone , 32,9 % na teren zabudowany , 7,4 % na wody , a 11,9 % na powierzchnie komunikacyjne .
Historia .
Założycielem miasta byli Rzymianie , którzy podczas wojen markomańskich , założyli na brzegu Dunaju obóz rzymski – Castra Romana .
