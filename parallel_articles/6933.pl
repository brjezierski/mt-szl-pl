Lionel Andrés Messi Cuccittini ( wym .
[ ] ; ur .
24 czerwca 1987 w Rosario ) – argentyński piłkarz , występujący na pozycji napastnika lub ofensywnego pomocnika .
Wieloletni zawodnik FC Barcelona , w 2021 został piłkarzem francuskiego klubu Paris Saint-Germain .
Kapitan reprezentacji Argentyny .
Karierę seniorską rozpoczął w młodzieżowym zespole FC Barcelony ( FC Barcelona C ) , skąd przeszedł do drugiego zespołu katalońskiego klubu .
W 2004 zanotował debiut dla pierwszej drużyny .
W sezonie 2008/2009 stał się kluczowym piłkarzem swojej drużyny , która po raz pierwszy w historii sięgnęła po potrójną koronę , zdobywając mistrzostwo Hiszpanii , puchar Hiszpanii i rozgrywki Ligi Mistrzów UEFA .
W sezonie 2014/2015 powtórzył osiągnięcie z 2008/2009 .
W 2018 został kapitanem FC Barcelony .
Pozostaje zawodnikiem z największą liczbą strzelonych goli i występów w historii FC Barcelony .
Do 2021 zdobył z klubem 35 trofeów : 10 tytułów Mistrza Hiszpanii , cztery puchary Ligi Mistrzów UEFA , siedem Pucharów Króla , ponadto siedmiokrotnie wygrał Superpuchar Hiszpanii , trzykrotnie Superpuchar Europy UEFA i trzy razy Klubowe Mistrzostwa Świata .
W 2021 został zawodnikiem francuskiego klubu Paris Saint-Germain .
