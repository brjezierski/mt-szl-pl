Liplas
Liplas – wieś w Polsce położona w województwie małopolskim , w powiecie wielickim , w gminie Gdów.Integralne części miejscowości : Podlesie , Podwale , Wachlówka .
W latach 1975–1998 miejscowość administracyjnie należała do województwa krakowskiego .
Wieś usytuowana jest Pogórzu Wielickim , nad potokiem Królewskim uchodzącym do Raby , w odległości 4,5 km od Gdowa .
Gleby są tu gliniaste na podłożu piaskowca naniesionego przez Rabę .
Po stronie północnej znajduje się wieś Jawczyce i Wiatowice , na wschodzie Niegowić , na zachodzie Bilczyce , na południu Gdów .
