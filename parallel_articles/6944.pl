Lipowy Dwór
Lipowy Dwór ( niem .
`` Lindenhof '' ) – osada w Polsce położona w województwie warmińsko-mazurskim , w powiecie giżyckim , w gminie Miłki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
Przed 1945 rokiem właścicielem majątku był `` Otto Bludau '' .
Według danych z przełomu lat 30. i 40. , posiadał on 278 hektarów pola , w tym 160 ha pól i ogrodów , 56 ha łąk , 50 ha pastwisk , 4 ha lasu do wyrębu i 8 ha nieużytków .
Po 1945 w Lipowym Dworze funkcjonował PGR , a majątek obecnie jest własnością prywatną .
W okolicy liczne bunkry i schrony , pochodzące z lat 30. , pozostałość po Giżyckim Rejonie Umocnionym .
Obok wsi znajduje się rów przeciwpancerny , zbudowany latem 1944 roku , w ramach rozbudowy systemu fortyfikacji .
Rów przeciwpancerny łączył się z siecią grobli na Łąkach Staświńskich .
