Liptowski Mikułasz
Liptowski Mikułasz ( słow .
`` Liptovský Mikuláš '' , do 1952 `` Liptovský Svätý Mikuláš '' ; węg .
`` Liptószentmiklós '' ; niem .
`` Sankt Nikolaus in der Liptau , Liptau-Sankt-Nikolaus '' ) – miasto powiatowe w północnej Słowacji , w kraju żylińskim .
Centrum gospodarcze , kulturalne i turystyczne historycznego regionu Liptów .
Miasto leży nad rzeką Wag w Kotlinie Liptowskiej między Górami Choczańskimi ( `` Chočské vrchy '' ) i Tatrami Zachodnimi na północy a Niżnymi Tatrami na południu .
Tuż na zachód od miasta leży wielki sztuczny zbiornik wodny Liptovská Mara .
Przez Liptowski Mikułasz przebiega słowacka droga krajowa nr 18 i równoległa do niej autostrada D1 z Popradu do Rużomberka .
Łączy się z nią droga lokalna nr 584 z Nižnej koło Twardoszyna .
Przez miasto przebiega również magistrala kolejowa , równoległa do drogi nr 18 / D1 .
Historia .
W okolicach Liptowskiego Mikułasza znaleziono wyroby z epoki brązu i późniejsze ( lateńskie i celtyckie ) , wskazujące na istnienie osadnictwa na tym terenie już dwa tysiące lat p.n.e .
Na przełomie IX i X wieku istniała tu słowiańska osada .
Pierwsza wzmianka o Liptowskim Mikułaszu pochodzi z 1286 r. z czasów Władysława IV Kumana .
Przez większość swych dziejów miasto należało do możnowładczego rodu Pongráczów .
W pierwszej połowie XIV wieku Liptowski Mikułasz uzyskał spore znaczenie handlowe , w 1424 r. otrzymał prawo organizowania targów dwukrotnie w ciągu roku .
W XVI wieku rozwinęło się rzemiosło .
