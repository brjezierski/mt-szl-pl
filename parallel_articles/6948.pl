Lipno ( Prudnik )
Lipno ( `` Lipy '' , ) – część miasta Prudnik , leżąca na prawym brzegu rzeki Prudnik , na południe od centrum miasta .
Geografia .
Lipno znajduje się około 1,8 km na południe od centrum Prudnika i około 3,2 km od granicy z Czechami , wzdłuż ulicy Józefa Poniatowskiego , w dolinie rzeki Prudnik , w północno-wschodniej części Gór Opawskich .
Zajmuje Kozią Górę i Kapliczną Górę .
Na wschód od Lipna znajduje się Młyn Czyżyka .
Historia .
Czasy niemieckie .
W XVIII wieku na południe od centrum Prudnika wybudowany został folwark .
Liczył on 1,62 km² .
W najstarszych przekazach kartograficznych występuje pod nazwą `` Piltz Vorwerk '' lub `` Buchen Vorwerk '' .
Później nazywany był `` Lindenvorwerk '' , czyli Lipowy Folwark .
Około 1780 roku w jego okolicy wybudowano kapliczkę poświęconą św .
Antoniemu .
W 1751 na szczycie Kaplicznej Góry Paweł Weidinger , wybudował murowaną kaplicę pw .
św .
Onufrego .
W 1753 postawiono murowaną pustelnię , stanowiąca zalążek klasztoru Kapucynów .
Wzgórze zyskiwało religijną popularność wśród wiernych z okolicznych miejscowości .
Sława pustelni rosła , jeszcze w XVIII wieku powstało tu sanktuarium Matki Bożej Bolesnej , odwiedzanej licznie przez okoliczną ludność z Prus i Austrii był tu m.in .
biskup wrocławski Filip Schaffgotsch .
Po sekularyzacji zakonów w państwie pruskim w 1819 roku władze kościelne urządziły w byłym klasztorze zakład karno-poprawczy dla księży .
Inspektorem w nim był Bonawentura Menzel .
Z czasem właścicielami folwarku została mieszczańska rodzina Fipperów , która wybudowała cegielnię w jego okolicy .
W 1899 wybudowali okazały pałac .
W 1896 na terenie Lipna oddano do użytku oczyszczalnię ścieków .
W 1927 Lipno zamieszkiwały 64 osoby .
Czasy polskie .
Po zakończeniu II wojny światowej do Lipna przyjechała komisja rewindykacyjna Ministerstwa Kultury i Sztuki w poszukiwaniu dóbr kultury ukrytych przez Günthera Grundmanna .
Zabudowania sanktuarium Matki Bożej Bolesnej doprowadzono do całkowitej ruiny .
Zabudowania zostały uszkodzone wskutek ostrzału artyleryjskiego w 1945 roku , a następnie dewastowane przez wojsko z pobliskiego poligonu i okolicznych mieszkańców .
W obecnym czasie na obszarze wzniesienia można znaleźć resztki murów budynków i dawnych kapliczek .
Po wojnie do dworu na Lipnie przyjeżdżała na żniwa młodzież z Komornik i Łowkowic .
W czasach PRL-u na Lipnie wybudowane zostały bloki mieszkalne .
1 października 1948 r. nadano miejscowości , wówczas administracyjnie związanej z Prudnikiem , polską nazwę Lipno .
Nazwa Lipno ma charakter martwy .
Mieszkańcy Prudnika używają wyłącznie nazwy Lipy .
Taka też nazwa dominuje w literaturze krajoznawczej i turystycznej .
