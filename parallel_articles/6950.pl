Lisa Gerrard
Lisa Gerrard ( ur .
12 kwietnia 1961 w Melbourne ) – australijska wokalistka i kompozytorka .
Życiorys .
W 1981 dołączyła do Brendana Perry'ego i założonej przez niego grupy Dead Can Dance .
W latach 80. brała udział w przedsięwzięciu This Mortal Coil .
W połowie lat 90. rozpoczęła karierę solową .
Obecnie jest znana głównie jako kompozytorka muzyki filmowej .
W 2001 wraz z Hansem Zimmerem otrzymała Złoty Glob oraz była nominowana do Oscara za muzykę do filmu `` Gladiator '' ( 2000 ) .
W 2005 współpracowała z Ennio Morricone przy muzyce do filmu `` Los utracony '' na podstawie książki Imre Kertésza .
Śpiewa kontraltem .
Piosenki najczęściej wykonywane są w jej własnym indywidualnym języku który wymyśliła i udoskonalała od 12 roku życia .
`` Wierzyłam że rozmawiam z Bogiem , kiedy śpiewałam w tym języku '' .
Rzadziej wykonuje utwory po angielsku .
Jej mężem jest Australijczyk o polskich korzeniach , Jacek Tuschewski .
Mają dwie córki , Teresę i Lashnę ( ur .
w 1992 roku ) .
