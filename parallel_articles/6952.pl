Lisewo ( przystanek kolejowy )
Lisewo – przystanek kolejowy w Lisewie Malborskim , w województwie pomorskim .
Do 2010 roku funkcjonował tutaj posterunek odgałęźny z trapezowym przejściem rozjazdowym , dzieląc wówczas szlak Tczew-Szymankowo na dwa odstępy , a do 1996 roku stacja przeładunkowa i punkt styczny z siecią Gdańskich Kolei Dojazdowych .
W roku 2017 przystanek obsługiwał 300–499 pasażerów na dobę .
Historia .
1 września 1939 w pobliżu stacji miała miejsce potyczka żołnierzy polskich z oddziałami Wermachtu zakończona zniszczeniem mostu kolejowego na Wiśle .
W związku z budową linii kolejowej E-65 w 2010 roku przebudowie uległ przystanek kolejowy w Lisewie Malborskim .
Zastąpiono wówczas jeden peron z dwiema krawędziami peronowymi , dwoma peronami , z których każdy posiada tylko jedną krawędź oraz wybudowano zadaszone przejście podziemne dla pieszych .
