Lisse
Lisse – duża miejscowość i gmina w zachodniej Holandii , w prowincji Holandia Południowa .
Gmina liczy 22 200 mieszkańców i zajmuje 16,11 km² .
W miejscowości znajduje się założony w 1949 roku największy ogród kwiatowy na świecie – `` Keukenhof '' .
Ogród zajmuje około 28 ha i corocznie wysadzanych jest tu ponad 7 mln cebulek kwiatów .
W Lisse znajduje się ponadto muzeum Zagłębia Cebulek Kwiatowych ( Museum voor de Bloembollenstreek ) .
Historia .
Pierwsze wzmianki o Lisse pojawiły się w 1198 roku .
Od początku XX wieku w Lisse zapoczątkowana została uprawa tulipana , dziś tutejszy ogród kwiatowy jest najbardziej znanym w całej Holandii .
