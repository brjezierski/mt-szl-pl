Lista przebojów Programu Trzeciego
Lista przebojów Programu Trzeciego ( `` Lista przebojów Trójki '' ) – polska radiowa lista przebojów nadawana na antenie Programu III Polskiego Radia od 24 kwietnia 1982 do 15 maja 2020 roku .
Audycja nadawana była w piątki lub soboty w godzinach wieczornych .
Historia .
Od 24 kwietnia 1982 do 27 lipca 2007 prowadzona była przez Marka Niedźwieckiego – prezentera do tej pory najbardziej kojarzonego z tą audycją .
Po jego odejściu z Polskiego Radia , od 3 sierpnia 2007 do 27 marca 2010 `` Listę ... '' prezentował Piotr Baron , jeden z tzw .
`` druhów zastępowych '' programu .
Od 5 kwietnia 2010 do 15 maja 2020 była prowadzona na zmianę przez obu dziennikarzy .
W wyniku wydarzeń związanych z notowaniem z 15 maja 2020 ( nr 1998 ) nie zostało wyemitowane kolejne notowanie .
Początkowo audycja nadawana była w soboty w godz .
20:00–22:00 .
We wrześniu 1989 ( od notowania 395 ) `` Listę '' wydłużono do 4 godzin ( godz .
18:00–22:00 ) .
We wrześniu 1990 `` Lista '' została przeniesiona na piątek ( od notowania 446 ) .
Od notowania 475 nadawano ją od godziny 19:00 do 22:00 .
