Jackrogersella cohaerens
Jackrogersella cohaerens ( Pers . )
L. Wendt , Kuhnert & amp ; M. Stadler – gatunek grzybów z rodziny `` Diatrypaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Jackrogersella , Hypoxylaceae , Xylariales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1794 roku Christiaan Hendrik Persoon nadając mu nazwę `` Sphaeria cohaerens '' .
W 1849 r. Elias Fries przeniósł go do rodzaju `` Hypoxylon '' jako `` Hypoxylon cohaerens '' .
Pod tą nazwą był znany w Polsce .
W 2005 r. Y.M .
Ju , J.D .
Rogers i H.M. Hsieh przeprowadzili rewizję rodzaju `` Hypoxylon '' , stwierdzili , że nie był on taksonem monofiletycznym i gatunek ten przenieśli do odrębnego rodzaju `` Annulohypoxylon '' .
Zaledwie 2 lata później jeszcze raz został on przez Wendta , Kuhnerta i M. Stadlera przeniesiony do nowo utworzonego rodzaju `` Jackrogersella '' .
Synonimy :
Morfologia .
Poduszkowata , zwężona u podstawy .
