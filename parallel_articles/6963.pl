Helvella latispora
Helvella latispora Boud .
– gatunek grzybów z rodziny piestrzycowatych ( `` Helvellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Helvella , Helvellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1898 r. Jean Louis Émile Boudier i nadana przez niego nazwa jest ważna do dzisiaj .
Synonimy :
Morfologia .
Składający się z kapelusza i trzonu .
Kapelusz o średnicy 1–4 cm i wysokości do 4,5 cm , początkowo złożony z dwóch nieregularnych płatów o brzegach podwiniętych do góry , potem mniej lub bardziej siodłowaty , z głęboką szczeliną lub zatoką , czasami z trzema płatami , lub nieregularnie klapowany .
Płodna powierzchnia górna naga , gładka , jasnobrązowa do płowożółtej lub biaława , sterylna powierzchnia dolna biaława .
Trzon o wysokości 1–7 cm , ale czasami aż do 13 cm , grubości 2–10 mm , w przybliżeniu cylindryczny , nagi , pusty wewnątrz , o powierzchni białawej do bladobrązowawej , przy podstawie z białą grzybnią .
Miąższ białawy , cienki , kruchy , bez wyraźnego zapachu i smaku .
Zarodniki 15–21 × 10–12,5 µm , eliptyczne , gładkie z jedną dużą kropla oleju .
Parafizy szkliste , cylindryczne z maczugowatymi lub tylko zaokrąglonymi wierzchołkami o szerokości 5–9 µm .
