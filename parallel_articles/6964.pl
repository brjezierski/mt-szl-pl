Żagwiak drobnopory
Żagwiak drobnopory , jamczatka drobnopora ( `` Podofomes stereoides '' ( Fr . )
Gorjón ) – gatunek grzybów z rzędu żagwiowców ( `` Polyporales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podofomes , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten opisał w 1874 roku Elias Fries , nadając mu nazwę `` Polyporus stereoides '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Sergio Pére Gorjón w 2020 r .
