Język litewski
Język litewski ( lit . )
– język z grupy języków bałtyckich , którym posługuje się ok. 4 mln osób .
Poza Litwą językiem tym posługują się Litwini zamieszkujący na zachodzie Białorusi i północno-wschodniej Polsce ( Suwalszczyzna ) , a także w Rosji , Łotwie oraz emigranci w USA , Kanadzie , Australii , Niemczech .
Jest językiem urzędowym Litwy .
Jako osobny język z zespołu wschodniobałtyckiego języków bałtyckich zaczął się rozwijać w VII wieku n.e .
Jako język pisany zaczął funkcjonować dopiero w XVI wieku .
Współczesna forma języka litewskiego ukształtowała się na przełomie XIX–XX wieku .
Język litewski jest bardzo interesujący dla językoznawców , gdyż podobnie jak język łotewski , zachował wiele archaicznych cech indoeuropejskich .
Pewien wpływ na ten język wywarły języki sąsiadujących narodów słowiańskich : język polski , język białoruski i język rosyjski , aczkolwiek w pierwszej połowie XX w. , po odzyskaniu niepodległości przez Litwę , można było zaobserwować tendencję do zastępowania slawizmów wyrazami rodzimymi .
Współczesny język litewski jest bardzo zróżnicowany pod względem dialektalnym , mimo stosunkowo niewielkiej liczby jego użytkowników zajmujących niezbyt duże terytorium .
Wyróżnia się dwa główne zespoły dialektów : żmudzki ( ) i auksztocki ( ) .
Główne różnice między tymi dialektami sprowadzają się do zjawisk z zakresu wokalizmu .
Dodatkowo w dialekcie auksztockim wyróżnia się trzy wielkie zespoły gwarowe : wschodni , południowy i zachodni ; każdy z nich obejmuje liczne podgwary , niekiedy znacznie różniące się między sobą .
Litewski nie jest wzajemnie zrozumiały z językiem łotewskim .
Różnice pomiędzy tymi dwoma językami są już znaczące nawet w warstwie podstawowych wyrazów .
