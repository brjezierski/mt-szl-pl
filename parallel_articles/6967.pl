Błonkóweczka powojnikowa
Błonkóweczka powojnikowa ( `` Litschauerella clematidis '' ( Bourdot & amp ; Galzin ) J. Erikss .
& amp ; Ryvarden ) – gatunek grzybów z rodziny `` Hydnodontaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Litschauerella , Hydnodontaceae , Trechisporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten po raz pierwszy opisali w 1913 r. Hubert Bourdot i Amédée Galzin nadając mu nazwę `` Peniophora clematidis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali Jakob Eriksson i Leif Ryvarden w 1976 r .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r ..
Morfologia .
Rozpostarty , biały , kremowy lub szary w postaci cienkiej , filcowanej i łuszczącej się powłoki .
Powierzchnia gładka , łuszcząca się , biała do ochrowej .
Kontekst miękki i bez wyraźnego zapachu .
Zarodniki prawie kuliste , gładkie , delikatnie brodawkowate , szkliste , 4,5–7 × 4–6 µm .
Występują grubościenne cystydy ( tramacystydy ) .
