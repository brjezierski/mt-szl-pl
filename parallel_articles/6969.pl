Litvínov ( stacja kolejowa )
Litvínov – stacja kolejowa w Litvínovie , w kraju usteckim , w Czechach .
Jest ważnym węzłem kolejowym .
Znajduje się na wysokości 305 m n.p.m .
Jest zarządzana przez Správę železnic .
Na stacji znajdują się kasy biletowe , na których istnieje możliwość zakupu biletów na wszystkie pociągi w tym międzynarodowe oraz rezerwacji miejsc .
