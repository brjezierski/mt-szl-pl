Litwini
Litwini ( lit .
`` lietuviai '' ) – naród bałtycki zamieszkujący głównie Litwę , a również Wielką Brytanię , USA , Irlandię , Brazylię i inne kraje , posługujący się językiem litewskim .
Nazwa `` Litwini '' jest pochodna od `` Litwy '' , która to nazwa geograficzna została po raz pierwszy zapisana w formie `` Litua '' w ok. 1008–1009 r. , w związku ze śmiercią misjonarza Brunona z Kwerfurtu .
Litwini powstali ze związku kilku pomniejszych grup plemiennych , wśród których najważniejszą rolę odgrywali Auksztoci i Żmudzini .
Powszechnie podaje się , że Litwini są najpóźniej schrystianizowanym narodem Europy ; nastąpiło to w XIV – XV wieku .
Obecnie większość Litwinów to katolicy obrządku rzymskiego .
Po okresie polonizacji – przede wszystkim elit , w związku z funkcjonowaniem Rzeczypospolitej Obojga Narodów , od II poł .
XIX w. , Litwini rozbudowują własną kulturę i język .
W okresie międzywojennym język litewski został poddany zabiegom oczyszczenia z wpływów języków słowiańskich ( głównie polskiego ) , ocenianych jako nadmierne .
Aktualnie liczbę Litwinów szacuje się na ok. 4,3 miliona , z uwzględnieniem emigracji , głównie do Stanów Zjednoczonych .
Litwini w Polsce zamieszkują Suwalszczyznę , głównie okolice Puńska i Sejn .
