Liverpool F.C .
Liverpool Football Club ( wym . )
– angielski klub piłkarski z siedzibą w Liverpoolu .
W sezonie 2022/2023 jego pierwsza drużyna występuje w Premier League .
19-krotnie zostawał mistrzem Anglii , 8-krotnie zdobywał Puchar Anglii oraz 9-krotnie Puchar Ligi Angielskiej .
Ponadto 6-krotnie wygrywał rozgrywki Pucharu Europy lub Ligi Mistrzów , co jest najlepszym osiągnięciem wśród wszystkich angielskich klubów .
Pod względem liczby triumfów w Pucharze Europy lub Lidze Mistrzów ustępuje jedynie hiszpańskiemu Realowi Madryt ( 14 zwycięstw ) i włoskiemu Milanowi ( 7 zwycięstw ) .
Liverpool F.C .
został założony w 1892 .
Od 1900 do 1947 , pięć razy zostawał mistrzem Anglii .
Pod koniec lat 50. , przez kilka sezonów grał na drugim szczeblu rozgrywek i do czasu zatrudnienia Billa Shankly ’ ego w 1959 , nie awansował do pierwszej ligi .
Klub tradycyjnie grał w strojach czerwono-białych , jednak w latach 60. usunięto barwę białą .
Pod wodzą Shankly ’ ego Liverpool trzykrotnie wygrywał rozgrywki ligowe , dwa razy zdobył Puchar Anglii oraz jeden raz Puchar UEFA ( było to pierwsze europejskie trofeum wywalczone przez klub ) .
Od 1977 do 1984 roku , czterokrotnie wygrywał Puchar Europy .
W latach 90. , zespół zanotował spadek formy , jednak w roku 2001 , zdobył małą potrójną koronę , zaś cztery lata później wygrał rozgrywki Ligi Mistrzów .
