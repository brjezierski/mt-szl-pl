Livorno
Livorno – miasto i gmina we Włoszech , w Toskanii , w prowincji Livorno .
Położone na wybrzeżu Morza Liguryjskiego .
Według danych na rok 2019 gminę zamieszkiwały 157 024 osoby , a gęstość zaludnienia wynosi 1502,62 os./km² .
W mieście znajduje się jeden z największych we Włoszech portów o znaczeniu handlowym i turystycznym .
Ważny ośrodek przemysłowy .
