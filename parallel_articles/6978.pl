Liw
Liw – wieś w Polsce położona w województwie mazowieckim , w powiecie węgrowskim , w gminie Liw .
Ośrodek krajoznawczy i letniskowy .
Dawniej miasto ; uzyskał lokację miejską przed 1421 rokiem , zdegradowany w 1869 roku .
Miejsce obrad sejmików ziemskich ziemi liwskiej od XVI wieku do pierwszej połowy XVIII wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Liw Stary i Liw Nowy były miastami królewskimi Korony Królestwa Polskiego , położonymi w drugiej połowie XVI wieku w powiecie liwskim ziemi liwskiej województwa mazowieckiego .
W czasach Rzeczypospolitej Obojga Narodów w Liwie mieściła się kasa szafarzy podatków dla Mazowsza .
Liw leży w północnej części Obniżenia Węgrowskiego , nad rzeką Liwiec .
Rzeka przepływająca przez miejscowość stanowi granicę dwóch historycznych dzielnic Podlasia i Mazowsza .
Wieś jest siedzibą rzymskokatolickiej parafii św .
