Liza Minnelli
Liza May Minnelli ( ur .
12 marca 1946 w Los Angeles ) – amerykańska aktorka , piosenkarka , tancerka i choreografka .
Życiorys .
Okres debiutu .
Minnelli urodziła się w Hollywood jako córka reżysera Vincente Minnellego oraz aktorki Judy Garland .
Zadebiutowała na ekranie w wieku trzech lat , w końcowej scenie filmu `` In The Old Good Summertime '' , w którym główną rolę zagrała jej matka .
Jako nastolatka Minnelli występowała w musicalach wystawianych na Broadwayu , zdobywając uznanie krytyki oraz publiczności .
W 1964 Minnelli rozpoczęła karierę muzyczną podpisując kontrakt z wytwórnią Capitol Records .
W tym samym roku wystąpiła u boku swojej matki na jednym z jej koncertów w Londynie .
Kilka miesięcy później ukazał się jej debiutancki album pt .
`` Liza !
Liza ! `` .
Płyta sprzedała się w prawie półmilionowym nakładzie .
Przełomem w karierze artystki była rola w filmie Alana J. Pakuli `` Bezpłodna kukułka '' ( 1969 ) , za którą otrzymała swoją pierwszą nominację do Oscara .
