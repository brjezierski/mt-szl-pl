Lizbona
Lizbona ( port .
`` Lisboa '' , wym .
[ ] ) – stolica i największe miasto Portugalii , położone w zachodniej części Półwyspu Iberyjskiego , nad rzeką Tag przy jej ujściu do Oceanu Atlantyckiego .
Miasto stanowi centrum polityczne , ekonomiczne i kulturalne kraju .
W centrum administracyjnym Lizbony mieszkało ( 2017 r. ) 505 526 mieszkańców , natomiast cała metropolia – Zespół Metropolitalny Lizbony ma 2 821 876 mieszkańców ( 27 % populacji państwa ) .
Region Lizbony jest najbogatszym regionem w Portugalii , wysoko ponad średnim PKB Unii Europejskiej – wytwarza 37 % PKB kraju .
Jest również politycznym centrum Portugalii , ponieważ ma tu swoją siedzibę rząd oraz głowa państwa .
Lizbona ma status metropolii o znaczeniu globalnym .
W badaniach dotyczących jakości życia , Lizbona uplasowała się na 18 miejscu wśród miast świata w 2015 roku według magazynu `` Monocle '' , na 43 miejscu wśród miast świata w 2017 roku według `` Mercer '' ( tuż za Londynem , Mediolanem i Barceloną , przed Nowym Jorkiem ) oraz na 7 miejscu wśród miast świata w 2017 roku według `` InterNations '' ' 2020 .
Została uznana jako Zielona Stolica Europy 2020 .
