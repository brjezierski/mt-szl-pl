Liévin
Liévin – miejscowość i gmina we Francji , w regionie Hauts-de-France , w departamencie Pas-de-Calais .
Według danych na rok 1990 gminę zamieszkiwały 33 623 osoby , a gęstość zaludnienia wynosiła 2621 osób/km² ( wśród 1549 gmin regionu Nord-Pas-de-Calais Liévin plasuje się na 15. miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu 187 . )
Miasto jest popularnie dzięki hali lekkoatletycznej , w której odbywają się znane mityngi .
W 1987 roku odbył się tutaj halowe mistrzostwa Europy w lekkoatletyce .
