Llanberis
Llanberis – wieś turystyczna w Wielkiej Brytanii , w Walii w hrabstwie Gwynedd u podnóża Snowdonu .
Ośrodek turystyki górskiej , kolarstwa górskiego o znaczeniu międzynarodowym .
Dobra infrastruktura turystyczna .
Język .
Według najnowszego spisu , 81 procent mieszkańców miejscowości mówi po walijsku .
Historia .
Osada powstała jako ośrodek górniczy ( wydobycie łupków ) .
Od momentu zamknięcia kopalni wieś rozwija się jako ośrodek turystyczny , centrum wypadów w masyw Snowdonu .
W 1895 wybudowano kolejkę na Snowdon .
Turystyka .
Llanberis jest do dziś popularnym punktem początkowym wypraw w region Snowdonii , ze szczególnym uwzględnieniem masywu Snowdonu .
Stanowi bazę dla krótkich i długich wycieczek .
