Lleida ( prowincja )
Lleida ( hiszp .
`` Lérida '' , okc .
`` Lhèida '' ) – prowincja Hiszpanii współtworząca wspólnotę autonomiczną Katalonii .
Powstała w roku 1833 .
Graniczy z prowincjami : Girona , Barcelona , Tarragona , Saragossa oraz Huesca , a także z Andorą i Francją .
Stolicą jest miasto o tej samej nazwie .
Prowincja liczyła w 2004 roku 385 092 mieszkańców , z czego 30 % zamieszkałych w stolicy .
Inne ważne miasta to m.in .
: La Seu d ’ Urgell , Mollerussa , Cervera i Tàrrega .
