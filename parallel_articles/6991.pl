Lobsang Sangay
Lobsang Sangay ( także Lobsang Sangje , tyb .
བློ་བཟང་སངས་རྒྱས་ wylie : `` blo-bzang sangs-rgyas '' ; ur .
1968 w Dardżylingu ) – tybetański prawnik i polityk , premier Tybetu w latach 2011–2012 , obecny przywódca polityczny Tybetu .
Życiorys .
Jego rodzice uciekli z Tybetu w 1959 .
Lobsang Sangay urodził się w Dardżylingu .
Ukończył szkołę średnią w osiedlu diaspory tybetańskiej , kształcił się również w St. Joseph 's College w rodzinnym mieście .
Studiował na Delhi University ( licencjat z prawa ) .
Działał w Kongresie Młodzieży Tybetańskiej .
Był wiceprzewodniczącym oddziału Kongresu w Dardżylingu ( 1988–1989 ) .
Pełnił również funkcję sekretarza generalnego ( 1989–1990 ) i przewodniczącego ( 1990–1991 ) struktur organizacji w Delhi .
W 1992 wszedł w skład Centralnego Komitetu Wykonawczego ( CENTREX ) TYC .
Zrezygnował z zasiadania w tym ciele w październiku 1994 .
W 1996 uzyskał , jako stypendysta Fulbrighta , stopień magistra , natomiast w 2004 stopień doktora na Harvard Law School .
Specjalizuje się w prawie międzynarodowym oraz w dziedzinie konstytucjonalizmu i rozwiązywania konfliktów .
27 kwietnia 2011 został wybrany nowym `` kalonem tripą '' ( otrzymał 27051 głosów ; 55 % wszystkich oddanych ) .
Pokonał Tenzina Namgjal Tethonga ( 18405 ; 37,42 % ) i Tasziego Wangdi ( 3173 ; 6,44 % ) .
8 sierpnia 2011 został zaprzysiężony .
16 września 2011 przedstawił parlamentowi na uchodźstwie kandydatów na `` kalonów '' ; zostali oni zaaprobowani przez deputowanych .
Tego samego dnia wszystkich , z wyjątkiem jednego , zaprzysiężono .
W stworzonym przez siebie gabinecie Lobsang Sangay objął tekę `` kalona '' edukacji .
20 września 2012 Tybetański Parlament na Uchodźstwie zmienił noszony przezeń tytuł na `` sikjong '' ( przywódca polityczny ) .
