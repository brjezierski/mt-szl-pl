Logo ( język programowania )
Logo ( gr .
λόγος [ `` logos '' ] – „ słowo , myśl ” ) – język programowania stworzony jako narzędzie do nauczania informatyki i matematyki .
Składa się z gotowych elementarnych procedur , które służą do definiowania procedur użytkownika .
Został zaprojektowany przez pracującego pod koniec lat 60 .
XX wieku na MIT Seymoura Paperta , który inspirował się pracami szwajcarskiego psychologa i pedagoga Jeana Piageta .
Język Logo oparty jest na LISP z inną składnią i używa tzw .
„ grafiki żółwia ” ( ang .
`` turtle graphics '' ) .
