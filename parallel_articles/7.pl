.al – krajowa domena internetowa najwyższego poziomu przypisana dla stron internetowych z Albanii , jest aktywna od 1992 roku i administrowana przez Radę Regulacji Telekomunikacji w Albanii .
W chwili obecnej nie jest możliwa rejestracja domeny drugiego poziomu , ale istnieje kilka domen już działających .
Są to : `` uniti.al '' , `` tirana.al '' , `` soros.al '' , `` upt.al '' oraz `` inima.al '' .
Można za to rejestrować domeny w domenach drugiego poziomu np .
`` domena.com.al '' .
