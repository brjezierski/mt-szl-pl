Lola Pagnani
Lola Pagnani ( ur .
3 kwietnia 1972 w Rzymie ) – włoska aktorka oraz tancerka baletowa .
Życiorys .
Urodzona w Rzymie jako Anna Lola Pagnani Stavros , córka pisarza i scenarzysty Enzo Pagnaniego .
Ukończyła paryską szkołę tańca współczesnego w wieku 17 lat , wzięła udział w światowym tournée grupy Momix , później pracowała nad choreografią Circe du Soleil w Montrealu .
Była główną tancerką monachijskiej opery Liny Wertmüller , dyrygowanej przez Giuseppe Sinopoliego .
Zaraz potem ukończyła Nowojorski Alvin Ailey American Dance Theater .
Po studiach aktorskich wróciła do Włoch .
We Włoszech współpracowała ze znanymi twarzami teatru i kina , jak Ettore Scola i Giulio Base ; występowała również dla Spike ' a Lee , Johna Turturro i Abla Ferrara .
Była również twarzą Lavazzy z Tullio Solenghi i Riccardo Garrone , później współpracowała z Maurizio Costanzo z jego telewizyjnym show .
Zaproszono ją do współpracy z Enrico Montesano , Marco Columbro , Barbarą de Rossi , Blas Roca Rey , Enrico Brignano , Nino Manfredi , Vittorio Gassman oraz Shelley Winters , która umożliwiła jej prywatne studia w Los Angeles .
