Lommedalen
Lommedalen - wiejska osada położona w pobliżu Bærum w okręgu Akershus w południowej części Norwegii , ok. 20 kilometrów na zachód od Oslo .
Nazwa osady pochodzi od rzeki Lomma .
Liczba mieszkańców wynosi ok. 3000 .
W Lommedalen dorastał ( urodzony w 1990 roku ) jeden z najbardziej utalentowanych szachistów w historii , Magnus Carlsen .
Rozgrywa się tu także część akcji powieści `` Palec Kasandry '' autorstwa Gerta Godenga .
