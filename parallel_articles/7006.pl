Lista płac
Lista płac – rodzaj dokumentu księgowego stosowany w rachunkowości potwierdzający naliczenie wynagrodzeń dla pracowników wraz ze wszystkimi obowiązującymi składkami płaconymi zarówno przez pracodawcę jak i pracownika .
Dokument ten może być podstawą rozchodu kasowego .
Nie ma nakazu sporządzania listy płac , jednak praktycznie wszyscy przedsiębiorcy tworzą ją co miesiąc by na jej podstawie wypłacić wynagrodzenia swym pracownikom .
Jest ona także punktem wyjścia dla prawidłowego wypełnienia deklaracji podatkowej oraz dokumentów ubezpieczeniowych , służy także poprawnemu zaksięgowaniu płac .
Gdy liczba pracowników jest duża , z reguły w liście płac ujmuje się zbiorcze wyliczenie pensji pracowników zakładu , a przedsiębiorstwa z niewielką liczbą zatrudnionych ograniczają się najczęściej do miesięcznego wypełnienia indywidualnych kart przychodów .
Karta przychodów .
Przedsiębiorcy zatrudniający pracowników , bez względu na formę organizacyjną , prowadzą karty przychodów .
Nie ma jednego , powszechnie obowiązującego , jej wzoru .
