Longueuil
Longueuil – miasto w Kanadzie , w prowincji Quebec , w regionie Montérégie , nie jest częścią żadnego MRC ( razem z kilkoma pobliskimi miastami tworzy terytorium równoważne Longueuil ) .
Miasto leży nad Rzeką Świętego Wawrzyńca naprzeciwko Montrealu i stanowi część jego aglomeracji .
Longueuil jest piątym co do wielkości miastem Quebecu i dziewiętnastym pod względem wielkości miastem Kanady .
Liczba mieszkańców Longueuil wynosi 229 330 .
Język francuski jest językiem ojczystym dla 80,1 % , angielski dla 6,8 % mieszkańców ( 2006 ) .
W mieście rozwinął się przemysł zbrojeniowy , metalowy oraz włókienniczy .
