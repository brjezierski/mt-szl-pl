Longyearbyen
Longyearbyen – stolica Svalbardu , liczy ponad 1,8 tys .
mieszkańców .
Ośrodek wydobycia węgla kamiennego , port lotniczy , centrum turystyki .
Historia .
Islandzkie kroniki podają informacje o odkryciu Svalbardu w 1194 .
Rosyjscy naukowcy utrzymują , że wyspa została zasiedlona przez rosyjskich osadników w XV wieku .
W 1596 do wyspy dotarł holenderski podróżnik Willem Barents .
W miejscu obecnego osiedla w XVI-XVIII wieku istniała osada myśliwska , w której mieszkali łowcy przygód i myśliwi z całej Europy Północnej .
Pod koniec XIX wieku odkryto tu bogate złoża surowców mineralnych , a w 1899 r. rozpoczęto wydobycie węgla kamiennego .
W 1906 osadę Longyearbyen założył John Munroe Longyear , główny właściciel firmy `` Arctic Coal Company of Boston '' .
Pierwszymi mieszkańcami osady było dwudziestu pięciu pracowników kopalni mieszkających w jednym baraku .
Z czasem dołączyli do nich kobiety z dziećmi .
W 1917 było już 141 mieszkańców .
W 1916 r. norweska firma `` Store Norske Spitsbergen Kulkompani '' odkupiła kopalnię i osadę Longyeara .
Norwegowie nazwali osadę Longyearbyen ( miasto Longyeara ) .
Osadnictwo ludzi niezwiązanych z wydobyciem węgla rozpoczęło się w ostatnim dwudziestoleciu XX wieku .
Na mocy podpisanego w 1920 roku Traktatu Spitsbergeńskiego cały archipelag oddano pod zarząd administracyjny Norwegii , która przejęła Svalbard w 1925 roku .
Longyearbyen stało się nieformalną stolicą Svalbardu .
Znaczny rozwój osady nastąpił po II wojnie światowej , kiedy odbudowano ją po zniszczeniach wojennych .
19 lipca 1985 statek pasażerski `` Maksim Gorki '' zderzył się z górą lodową .
