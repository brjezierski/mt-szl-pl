Lophodermium conigenum
Lophodermium conigenum ( Brunaud ) Hilitzer – gatunek grzybów z rodziny łuszczeńcowatych ( `` Rhytismataceae '' ) .
Jeden z patogenów wywołujących chorobę sosny ( `` Pinus '' ) o nazwie wiosenna osutka sosny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lophodermium , Rhytismataceae , Rhytismatales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1888 r. Paul Brunaud nadając mu nazwę `` Lophodermium pinastri '' f. `` conigenum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Alfred C. Hilitzer w 1929 r .
Synonimy :
Morfologia .
