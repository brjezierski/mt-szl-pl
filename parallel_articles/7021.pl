Język lop
Język lop , także lopnur lub lopnor — język wschodnioturecki używany w Regionie Autonomicznym Sinciang-Ujgur Chińskiej Republiki Ludowej , na terenie powiatu Lop w prefekturze hoteńskiej .
Uważa się go za wschodni dialekt języka ujgurskiego .
Wykazuje podobieństwa z północnymi dialektami języka kirgiskiego .
