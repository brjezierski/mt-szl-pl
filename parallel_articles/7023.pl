Lordi
Lordi – fiński zespół muzyczny założony w 1996 roku , zwycięzca 51 .
Konkursu Piosenki Eurowizji z utworem „ Hard Rock Hallelujah ” .
Historia .
Zespół został założony w 1996 , jego założyciele poznali się w Szwecji na koncercie zespołu Kiss .
Pierwszą płytą Lordi miał być krążek pt .
`` Bend Over and Pray the Lord '' , ale po jego nagraniu okazało się , że wytwórnia nie może go wydać .
Debiutancki album pt .
`` Get Heavy '' został wydany w 2002 pod szyldem BMG Finland .
Już pierwszy singel ( „ Would You Love a Monsterman ? ” ) zapewnił zespołowi na starcie dużą popularność w Finlandii .
W 2004 z inicjatywy lidera grupy powstał krótkometrażowy film grozy pt .
`` The Kin '' , w którym pięcioro muzyków wystąpiło w swoich scenicznych strojach w roli potworów .
