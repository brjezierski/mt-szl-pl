Port lotniczy Tân Sơn Nhất w Ho Chi Minh
Port lotniczy Tân Sơn Nhất ( IATA : SGN , ICAO : VVTS ) – międzynarodowy port lotniczy położony 7 km na północ od Ho Chi Minh .
Jest największym portem lotniczym Wietnamu .
W 2006 obsłużył 15,5 mln pasażerów .
Ma pojemność 15-17 mln pasażerów rocznie , w porównaniu do zdolności Hanoi - Noi Bai 8 mln pasażerów i Đà Nẵng 2 mln pasażerów .
Jest to także największy port lotniczy w Wietnamie w zakresie obsługi pasażerów ( z szacunkową liczbę ponad 12,5 mln pasażerów rocznie w roku 2009 , co stanowi ponad połowę ruchu pasażerskiego w Wietnamie ) .
Jego kod IATA SGN pochodzi od poprzedniej nazwy miasta Sajgonu .
W 2010 r. lotnisko obsłużyło 15,5 mln pasażerów i 130 tys .
