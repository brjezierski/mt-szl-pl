Louis Pasteur
Louis Pasteur ( pol .
Ludwik Pasteur ; ur .
27 grudnia 1822 w Dole , zm .
28 września 1895 w Saint-Cloud ) – francuski chemik i prekursor mikrobiologii .
Laureat Medalu Copleya .
Życiorys .
Był synem garbarza Jeana-Josepha Pasteura ( 1791–1865 ) i Jeanne-Étienne Roqui ( 1793–1848 ) .
Dzieciństwo spędził w Arbois przy granicy szwajcarskiej .
Do liceum uczęszczał w Besançon .
Studia przyrodnicze ukończył w École normale supérieure w Paryżu pod kierunkiem profesorów Dumasa i Balarda .
W ramach stażu po zakończeniu nauki pracował jako nauczyciel fizyki w liceum w Dijon .
W 1849 przeniósł się do Strasburga , gdzie otrzymał nominację na docenta .
W tym samym roku poślubił Marie Laurent .
Mieli pięcioro dzieci : Jeanne , Jeana Baptiste ’ a , Cécile , Marie-Louise i Camille .
Troje z nich zmarło przedwcześnie ( Jeanne , Camille i Cécile ) .
W 1854 ze Strasburga przeprowadził się do Lille , gdzie rozpoczął pracę na nowo otwartym uniwersytecie .
Rok 1857 przyniósł nominację na rektora uniwersytetu w Paryżu .
