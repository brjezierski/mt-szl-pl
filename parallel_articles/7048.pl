Loveland ( Ohio )
Loveland – miasto w Stanach Zjednoczonych , w południowo-zachodniej części stanu Ohio , w hrabstwie Hamilton .
W 2000 roku liczba ludności wynosiła 11 771 .
Miasto zostało nazwane na cześć Jamesa Lovelanda .
W Loveland znajduje się 16 parków miejskich .
Geografia .
Współrzędne geograficzne miasta to 39°16′8″ szerokości północnej i 84°16′13″ długości zachodniej ( 39,268759 , -84,270397 ) .
Według United States Census Bureau , powierzchnia miasta wynosi 12,2 km² , z czego 12,0 km² to ląd , a 0,2 km² to woda .
Woda stanowi 1,28 % powierzchni .
Demografia .
Według spisu powszechnego z roku 2000 , miasto zamieszkuje 11 677 osób i 3 224 rodzin .
Gęstość zaludnienia wynosi 969,6/km² .
W mieście jest 4497 gospodarstw domowych i 4 653 jednostek mieszkalnych o zagęszczeniu 386,4 jednostek/km² .
95,66 % mieszkańców należy do rasy białej , 1,56 % to osoby należące do rasy czarnej lub Afroamerykanie , 0,05 % to rdzenni Amerykanie lub Indianie , 1,05 % Azjaci , 0,42 % do innej rasy , zaś 1,26 % do dwóch lub więcej ras .
1,12 % ludności to Latynosi lub osoby pochodzenia hiszpańskiego .
