Luanda
Luanda – stolica i największe miasto Angoli , położone w północno-zachodniej części kraju nad Oceanem Atlantyckim .
W 2008 roku była uznana za najdroższe miasto świata .
Luanda została założona przez portugalskiego żeglarza Paulo Diasa de Novais 25 stycznia 1576 r. pod nazwą São Paulo da Assumpção de Loanda .
Polskie źródła podają , że było to w roku 1573 pod nazwą São Paulo de Loanda .
Na przełomie XVII i XVIII wieku Luanda była ważnym ośrodkiem handlu niewolnikami w tej części Afryki .
W 1975 po uzyskaniu niepodległości przez Angolę Luanda stała się jej stolicą .
Jest największym ośrodkiem gospodarczym i naukowo-kulturalnym kraju .
