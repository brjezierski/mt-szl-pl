Lubaczów
Lubaczów ( , `` Lubacziw '' , ) – miasto w południowo-wschodniej Polsce , w województwie podkarpackim , siedziba powiatu lubaczowskiego , położone na Płaskowyżu Tarnogrodzkim , u ujścia rzeki Sołotwy do Lubaczówki , blisko granicy państwa z Ukrainą .
Leży w historycznej ziemi bełskiej .
Miasto stanowi centrum życia społeczno-gospodarczego i kulturalnego regionu .
Stanowi siedzibę samodzielnej gminy miejskiej .
Jednocześnie jest również siedzibą władz gminy wiejskiej , w której skład wchodzą 23 sołectwa .
