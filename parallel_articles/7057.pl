Lubatka ( strumień )
Lubatka ( do 1945 niem .
`` Ruhnauer Bach '' , `` Lubatka '' , `` Uller '' ) – strumień w powiecie żarskim i żagańskim na południu województwa lubuskiego , lewy dopływ Czernej .
Źródła strumienia znajdują się na południowym krańcu Wzniesień Żarskich na podmokłych terenach w okolicy Mirostowic Górnych .
Lubatka przepływa przez Stawnik i Mirostowice Dolne a następnie przez Jankowę Żagańską , Szczepanów , Wilkowisko i uchodzi do Czernej w Żagańcu w Borach Dolnoślaskich .
