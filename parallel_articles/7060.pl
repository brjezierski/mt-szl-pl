Nowe Miasto Lubawskie
Nowe Miasto Lubawskie ( ) – miasto w Polsce położone w województwie warmińsko-mazurskim , siedziba powiatu nowomiejskiego .
Położone jest między Garbem Lubawskim a Pojezierzem Iławskim , nad rzeką Drwęcą .
Według danych z 31 grudnia 2021 Nowe Miasto Lubawskie liczyło 10 554 mieszkańców .
Bazując na danych ze spisu powszechnego a ujętych na portalu polskawliczbach.pl aglomeracja Nowego Miasta Lubawskiego liczy 19 380 mieszkańców .
Do aglomeracji można wliczyć takie miejscowości , jak : Kurzętnik , Bratian , Marzęcice , Pacółtowo , Mszanowo , Lipowiec , Łąki Bratiańskie , Nawra oraz Kaczek .
W latach 1998-2021 liczebność aglomeracji wzrosła o 1510 osób .
Nowe Miasto Lubawskie uzyskało lokację miejską w 1325 roku .
