Lubań ( rejon wilejski )
Lubań ( ; ) – agromiasteczko na Białorusi , w obwodzie mińskim , w rejonie wilejskim .
W 2009 roku liczyła 984 mieszkańców .
Na terenie wsi znajduje się zabytkowy dworek z XIX wieku oraz drewniana cerkiew prawosławna pw .
Soboru Świętych Białoruskich , przy której działa parafia .
Z miejscowości pochodzi Jan Kanty Lubański , polski ziemianin , bankier , działacz społeczny i filantrop .
Parafia rzymskokatolicka .
Parafia leży w dekanacie wilejskim archidiecezji mińsko-mohylewskiej .
Została zarejestrowana w 2007 r. Nabożeństwa odbywają się w prywatnym domu przystosowanym do celów liturgicznych .
