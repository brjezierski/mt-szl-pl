Jego nazwa upamiętnia pułkownika Thomasa Saltusa Lubbocka .
Miasto rozwinęło się jako ośrodek ranczerski .
Lubbock jest stolicą hrabstwa Lubbock i leży w regionie Llano Estacado , niezwykle ważnym dla branży bawełnianej .
Olbrzymie plantacje nawadniane są za pomocą sztucznych kanałów .
W mieście rozwinął się przemysł chemiczny oraz spożywczy .
