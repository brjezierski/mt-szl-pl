Opole Lubelskie
Opole Lubelskie – miasto w województwie lubelskim , w Kotlinie Chodelskiej .
Siedziba powiatu opolskiego i siedziba gminy miejsko-wiejskiej Opole Lubelskie .
Ośrodek przemysłu spożywczego ( mleczarnia , fabryki słodyczy , zakład przetwórstwa owoców ) .
Pod względem historycznym Opole Lubelskie położone jest w Małopolsce , w dawnej ziemi lubelskiej , początkowo leżało w województwie sandomierskim , zaś od 1474 roku w województwie lubelskim .
Prywatne miasto szlacheckie lokowane w 1418 roku , położone było w drugiej połowie XVI wieku w powiecie lubelskim w województwie lubelskim .
W latach 1975–1998 miasto administracyjnie należało do województwa lubelskiego .
Według danych GUS z 31 grudnia 2019 r. Opole Lubelskie liczyło 8388 mieszkańców .
