Województwo lubelskie ( I Rzeczpospolita )
Województwo lubelskie – jednostka terytorialna Korony Królestwa Polskiego , później Rzeczypospolitej Obojga Narodów istniejąca w latach 1474–1795 , część prowincji małopolskiej .
Obejmowała powierzchnię posiadając 3 powiaty .
Siedzibą wojewody oraz miejscem odbywania sejmików ziemskich był Lublin .
Województwo utworzył król Kazimierz IV Jagiellończyk w 1474 r. poprzez wyłączenie z województwa sandomierskiego .
Herb województwa przedstawiał białego jelenia na czerwonym tle .
Dewizą województwa jest łacińska sentencja : „ Parata semper cornua ” , która oznacza „ Rogata zawsze gotowa ” .
Słowa te należałoby rozumieć jako : rycerska chorągiew Ziemi Lubelskiej , mająca w swoim znaku rogatego skaczącego jelenia , jest zawsze gotowa i skora do walki .
Główne miasta : Lublin , Urzędów , Kurów , Kazimierz Dolny , Łuków , Parczew .
Urzędy .
Pierwszym wojewodą został Dobiesław Kmita .
Województwo lubelskie posiadało dwóch senatorów : wojewodę i kasztelana lubelskiego .
Sejmiki odbywały się w Lublinie .
