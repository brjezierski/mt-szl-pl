Janów Lubelski
Janów Lubelski – miasto w województwie lubelskim , w powiecie janowskim , siedziba gminy miejsko-wiejskiej Janów Lubelski .
Pod względem historycznym Janów Lubelski leży w dawnej ziemi lubelskiej w Małopolsce , wieś Biała , która w 1640 dała początek miastu , w drugiej połowie XVI wieku położona była w powiecie lubelskim województwa lubelskiego .
W latach 1975–1998 miasto administracyjnie należało do województwa tarnobrzeskiego .
Według danych GUS z 31 grudnia 2020 r. Janów Lubelski liczył 11 661 mieszkańców .
Położenie .
Janów Lubelski położony jest na pograniczu Roztocza Zachodniego , południowego skraju Wyżyny Lubelskiej oraz Równiny Biłgorajskiej , czyli północnego krańca Kotliny Sandomierskiej .
Według danych z 1 stycznia 2011 r. powierzchnia miasta wynosiła 14,80 km² .
Miasto znajduje się nad rzeką Białką .
Na południe od Janowa Lubelskiego rozciągają się Lasy Janowskie , stanowiące zachodnią część kompleksu leśnego Puszczy Solskiej .
