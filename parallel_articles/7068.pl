Tomaszów Lubelski
Tomaszów Lubelski – miasto w województwie lubelskim , siedziba powiatu tomaszowskiego oraz gminy wiejskiej Tomaszów Lubelski .
Leży na Roztoczu Środkowym , nad Sołokiją .
Według danych GUS z 31 grudnia 2020 r. Tomaszów Lubelski liczył 18 783 mieszkańców .
W mieście funkcjonuje przemysł środków transportu , spożywczy ( przetwórstwo owoców i warzyw ) .
Węzeł dróg wojewódzkich nr 850 oraz nr 853 , a także drogi krajowej nr 17 .
Miasto jest bazą wypadową na Roztocze .
Tomaszów Lubelski leży na historycznej Rusi Czerwonej , w dawnej ziemi bełskiej , stanowiącej wcześniej księstwo bełskie .
Prywatne miasto szlacheckie lokowane w 1621 roku .
W XVI wieku położone było na terenie województwa bełskiego .
