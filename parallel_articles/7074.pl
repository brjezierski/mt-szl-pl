Lublana ( powieść )
Lublana – dwutomowa powieść historyczna o czasach przedpiastowskich , napisana przez Józefa Ignacego Kraszewskiego w 1878 r. w Dreźnie ( nie należąca do cyklu `` Dzieje Polski '' ) .
Dedykowana Marii Ilnickiej – założycielce i pierwszej redaktorce pisma kobiecego „ Bluszcz ” , w którym drukowano ją w 1879 r. ( nr 1-29 ) .
W wydaniu książkowym ukazała się w tymże roku w warszawskim wydawnictwie Michała Glücksberga .
Później nie wznawiana .
Kolejne wydanie dopiero w 1986 r. , w edycji dzieł J.I .
Kraszewskiego przygotowanej pod kierunkiem prof. Czesława Hernasa .
Treść .
Wątek przewodni stanowi miłość młodego zamożnego kmiecia Mirka do córki bogatego i wpływowego kmiecia Ryżca – dumnej i samowolnej Lublany .
