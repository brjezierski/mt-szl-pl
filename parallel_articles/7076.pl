Lubliniec
Lubliniec ( pierwotnie : `` Lubin '' i `` Lublin Śląski '' , niem .
`` Lublinitz '' [ do 1922 , 1939–1941 ] , `` Loben '' [ 1941–1945 ] , , łac .
`` Lobin '' ) – miasto w Polsce w województwie śląskim , siedziba władz powiatu lublinieckiego .
Główny ośrodek gospodarczy , turystyczny , kulturalny , edukacyjny , rolniczy i przemysłowy ziemi lublinieckiej oraz drugi co do wielkości ( po Częstochowie ) w północnej części województwa śląskiego .
Jedno z najstarszych miast Śląska , największe miasto Równiny Opolskiej oraz stolica tzw .
Śląska Białego .
Położenie .
