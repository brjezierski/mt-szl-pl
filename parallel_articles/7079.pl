Luboml
Luboml ( ukr .
`` Лю́бомль '' ) – miasto na Ukrainie , w obwodzie wołyńskim , siedziba rejonu lubomelskiego .
Miasto królewskie lokowane w 1287 roku położone było w XVI wieku w województwie ruskim .
Leży w historycznej ziemi chełmskiej .
Do 1945 w Polsce , w województwie wołyńskim , siedziba powiatu lubomelskiego i gminy Luboml .
Historia .
Pierwsze wzmianki pochodzą z około 1287 roku z Latopisu Ipatiewskiego , wymieniającego Luboml jako gród ruski należący do księcia Haliskiego .
W 1356 wszedł w posiadanie Polski i został nadany w dzierżawę lenną księciu bełzkiemu Jerzemu Narymuntowiczowi .
