Lubotyń ( jezioro )
Lubotyń – jezioro w woj .
wielkopolskim , w pow .
kolskim , w gminie Babiak , leżące na terenie Pojezierza Kujawskiego .
Według urzędowego spisu opracowanego przez Komisję Nazw Miejscowości i Obiektów Fizjograficznych ( KNMiOF ) nazwa tego jeziora to `` Lubotyń '' .
W różnych publikacjach i na mapach topograficznych jezioro to występuje pod nazwą `` Jezioro Lubotyńskie '' .
