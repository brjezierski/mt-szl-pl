Województwo lubuskie
Województwo lubuskie – województwo w zachodniej Polsce , utworzone w 1999 roku – w wyniku reformy administracyjnej – z większości terytoriów dawnych województw : gorzowskiego i zielonogórskiego oraz niewielkiej części leszczyńskiego , a funkcjonujące od 1 stycznia 1999 .
Siedzibą wojewody jest Gorzów Wielkopolski , zaś władz samorządu województwa – Zielona Góra .
Od północy graniczy z woj .
zachodniopomorskim , od wschodu – z woj .
wielkopolskim , od południa – z woj .
dolnośląskim , a od zachodu – z Niemcami .
Według danych na 31 grudnia 2018 , obejmuje obszar o powierzchni 13 987,93 km² , a jego populacja wynosi poniżej 1 mln mieszkańców według danych z 30 czerwca 2020 ( co czyni je drugim po województwie opolskim najmniej liczebnym województwem w Polsce ) .
Posiada największą lesistość w kraju .
Nazwa województwa wywodzi się od krainy historycznej – ziemi lubuskiej .
W skład województwa wchodzą ziemie czterech krain historycznych – ziemi lubuskiej , Dolnego Śląska , Wielkopolski i Łużyc Dolnych .
Pomimo nazwy nie zawiera Lubusza , znajdującego się na terytorium Niemiec .
Historia .
1945–1998 .
6 lipca 1950 , w wyniku reformy administracyjnej , z zachodniej części województwa poznańskiego i fragmentu województwa wrocławskiego utworzono województwo zielonogórskie , pokrywające się w przybliżeniu z granicami obecnego woj .
