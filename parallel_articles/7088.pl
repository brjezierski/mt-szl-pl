Huta Lubycka
Huta Lubycka – wieś w Polsce , położona w województwie lubelskim , w powiecie tomaszowskim , w gminie Lubycza Królewska .
W latach 1975–1998 wieś administracyjnie należała do województwa zamojskiego .
Wieś tenuty rzeczyckiej , położona była w XVIII wieku w powiecie bełskim .
Wieś stanowi sołectwo gminy Lubycza Królewska obejmujące Hutę Lubycką , Mrzygłody Lubyckie , Pawliszcze .
Historia .
Huta Lubycka w wieku XIX wieś w powiecie Rawa Ruska .
W roku 1881 wieś posiadła przestrzeni dworskiej 84 morgi , włościańskiej 477 mórg .
Ludność wyznania rzymskokatolickiego w liczbie 145 , należała do parafii w Lubyczy-Kniazie , sąd powiatowy miał siedzibę w Rawie , urząd pocztowy w Lubyczy Królewskiej , oddalonej o 14 kilometrów .
Właścicielem większej posiadłości była gmina Lubycza Kniazie .
