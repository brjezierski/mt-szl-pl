Lucka
Lucka – miasto w Niemczech , w kraju związkowym Turyngia , w powiecie Altenburger Land .
Lucka jest podzielona na dwie dzielnice : Breitenhain i Prößdorf .
Historia .
Pierwsze osady w okolicach Lucki pochodzą z wczesnej epoki kamienia ( 5000-2500 p.n.e . ) .
Pierwsze pisemne relacje o miejscowości pochodzą z 1320 jako `` opidum Luckowe '' .
Okolice były polem bitwy pod Lucka stoczonej w 1307 pomiędzy Habsburgami i Wettynami .
