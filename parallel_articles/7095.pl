Luckau
Luckau ( , wym .
; pol .
hist .
`` Łuków '' ) – miasto we wschodnich Niemczech , w kraju związkowym Brandenburgia , w powiecie Dahme-Spreewald .
Geografia .
Historycznie położone jest na Dolnych Łużycach .
Luckau leży na północno-zachodnich krańcach Wzniesień Łużyckich `` ( Lausitzer Grenzwall ) '' nad rzeką Berste , około 75 kilometrów na południe od Berlina , 45 kilometrów na północny zachód od Chociebuża i nieco ponad 100 kilometrów na północny wschód od Lipska .
Miasto powiatowe Lubin jest oddalone o 16 kilometrów .
