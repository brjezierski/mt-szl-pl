Lucy Maud Montgomery
Lucy Maud Montgomery ( ur .
30 listopada 1874 w Clifton na Wyspie Księcia Edwarda , zm .
24 kwietnia 1942 w Toronto ) – kanadyjska pisarka , autorka m.in .
cyklu powieściowego `` Ania z Zielonego Wzgórza '' .
Biografia .
Lucy Maud Montgomery ( używała jedynie drugiego imienia – Maud ) urodziła się 30 listopada 1874 roku w Clifton na Wyspie Księcia Edwarda jako jedyna córka Hugh Johna Montgomery ’ ego i Clary Woolner Macneill Montgomery .
Kiedy miała 21 miesięcy , jej matka zmarła na gruźlicę .
Ojciec przeniósł się na zachód Kanady , oddawszy dziecko pod opiekę swoim teściom Alexandrowi Marquis Macneill i Lucy Woolner Macneill , zamieszkałym w rybackiej wiosce Cavendish na Wyspie Księcia Edwarda .
Dzieciństwo i młodość ( z krótką przerwą na początku lat 90 .
XIX wieku ) przyszła pisarka spędziła z dziadkami .
Byli to ludzie o surowych zasadach – nie potrafili okazać wnuczce troski i uczuć .
