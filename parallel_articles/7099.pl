Mleczajowiec chrząstka
Mleczajowiec chrząstka ( `` Lactifluus vellereus '' ( Fr . )
Kuntze , mleczaj chrząstka ) – gatunek grzybów należący do rodziny gołąbkowatych ( `` Russulaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lactarius , Russulaceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1821 r. Elias Fries nadając mu nazwę `` Agaricus vellereus '' .
Ten sam autor w 1838 r. przeniósł go do rodzaju `` Lactarius '' , a Otto Kuntze w 1891 do rodzaju `` Lactifluus '' .
Niektóre synonimy łacińskie :
Nazwę polską mleczaj chrząstka podał Franciszek Błoński w 1889 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też pod nazwami : chrząszcz , chrząstka , gniewosz , hrózd .
