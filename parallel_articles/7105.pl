Ludwig van Beethoven
Ludwig van Beethoven , wymowa niemiecka : ( ur .
15–17 grudnia ( zob .
niżej ) 1770 w Bonn , zm .
26 marca 1827 w Wiedniu ) – niemiecki kompozytor i pianista , ostatni z tzw .
klasyków wiedeńskich , a zarazem prekursor romantyzmu w muzyce , uznawany za jednego z największych twórców muzycznych wszech czasów .
Urodzony w Bonn , na terenie dzisiejszych Niemiec , już w młodości przeniósł się do Wiednia , gdzie rychło uzyskał reputację pianisty wirtuoza , a następnie wybitnego kompozytora .
Mimo że około 25. roku życia zaczął tracić słuch , okazał wielką siłę ducha i nie zaprzestał tworzenia ( do czasu również wykonywania ) swoich dzieł , nawet w okresie całkowitej głuchoty .
W historii muzyki stanowi centralną postać pogranicza klasycyzmu i romantyzmu .
Jego twórczość wytyczyła kierunek rozwoju muzyki na cały XIX wiek .
Światową popularność Beethoven zawdzięcza przede wszystkim swym dziewięciu symfoniom , choć równie przełomowe było znaczenie jego sonat i wariacji fortepianowych oraz muzyki kameralnej , przede wszystkim kwartetów smyczkowych .
Muzyka Beethovena łączy intensywność uczuć z doskonałością formy ; wyraża się w niej apoteoza wolności i godności jednostki , woli życia , heroizmu , zmagań z losem oraz radości i powszechnego braterstwa .
