Ludwig von Mises
Ludwig Heinrich Edler von Mises ( ur .
29 września 1881 we Lwowie , zm .
10 października 1973 w Nowym Jorku ) – ekonomista austriacki .
Brat matematyka Richarda von Misesa .
Przedstawiciel szkoły austriackiej w ekonomii , w poglądach na gospodarkę reprezentował stanowisko leseferystyczne .
Twierdził , że niemożliwe jest racjonalne funkcjonowanie gospodarki planowej .
Stanowisko to wywołało w okresie międzywojennym szeroką dyskusję teoretyczną ( Maurice Dobb , Oskar Lange ) .
Życiorys .
Urodził się 29 września 1881 w domu nr 13 przy ulicy Jagiellońskiej we Lwowie , w żydowskiej rodzinie Artura i Adeli ( z domu Landau ) Edler von Misesów .
Ojciec Ludwiga był absolwentem Politechniki w Zurychu .
Potem pracował w austriackim Ministerstwie Kolei jako inżynier .
Ludwig był najstarszym z trójki chłopców .
Jeden z braci umarł w dzieciństwie , natomiast Richard został znanym matematykiem .
W latach 1892–1900 Ludwig von Mises uczęszczał do prywatnej szkoły podstawowej we Lwowie .
Otrzymał tam klasyczne wykształcenie , dodatkowo wzbogacone kulturą galicyjskiej stolicy .
Przed ukończeniem dwunastego roku von Mises obyty był w piśmie niemieckim , łacińskim , cyrylicy , grece oraz hebrajskim .
Władał przy tym także językiem polskim i francuskim oraz rozumiał po ukraińsku .
Po ukończeniu szkoły podstawowej kontynuował edukację w stolicy cesarstwa , Wiedniu , gdzie uczęszczał do publicznego gimnazjum , zaś w 1900 rozpoczął studia na Uniwersytecie Wiedeńskim .
W 1903 umarł jego ojciec .
20 lutego 1906 otrzymał stopień doktora prawa kanonicznego i rzymskiego na tymże uniwersytecie .
