( 4583 ) Lugo
( 4583 ) Lugo – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 3 lat i 224 dni w średniej odległości 2,35 j.a .
Została odkryta 1 września 1989 roku w Narodowym Obserwatorium Astronomicznym Rożen .
Nazwa planetoidy pochodzi od `` Raymonda Lugo '' ( ur .
1957 ) , zastępcy kierownika misji NASA New Horizons skierowanej do Plutona i pasa Kuipera .
Przed jej nadaniem planetoida nosiła oznaczenie tymczasowe ( 4583 ) 1989 RL4 .
