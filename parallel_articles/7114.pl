Luiz Alves
Luiz Alves – miasto i gmina w południowo-wschodniej Brazylii , w stanie Santa Catarina .
Znajduje się w mezoregionie Vale do Itajaí i mikroregionie Blumenau .
Położone jest około 100 km w linii prostej od Florianópolis .
Miejscowość leży w dolinie rzeki Rio Luís Alves .
Znane jest z produkcji Cachaça i związanego z nim corocznego festiwalu Fenaca – Festa Nacional da Cachaça .
Luiz Alves znane jest także z produkcji bananów .
Kolonizacja tych terenów rozpoczęła się w 1877 r. za sprawą włoskich imigrantów .
18 lipca 1958 r. miejscowość uzyskała prawa miejskie .
