Luizjana
Luizjana ( ang .
`` Louisiana '' ; wym . )
– stan na południu Stanów Zjednoczonych , u ujścia rzeki Missisipi do Zatoki Meksykańskiej .
Ma powierzchnię 135 382 km² , z czego 21 455 km² to obszary wodne .
Luizjana graniczy na północy z Arkansas , na wschodzie z Missisipi , a na zachodzie z Teksasem .
Jej stolicą jest Baton Rouge , a największym miastem Nowy Orlean ( ponad 1,2 mln mieszkańców w obszarze metropolitalnym ) .
Nad Zatoką Meksykańską rozpościerają się obszary nizinne .
W części południowo-wschodniej duży obszar zajmuje delta Missisipi .
Na wybrzeżu występują liczne jeziora i bagna .
Historia .
Przed przybyciem Europejczyków terytorium Luizjany było zamieszkane przez liczne plemiona Indian i do dzisiaj wiele miejscowości ma nazwy pochodzące od nazw indiańskich .
Rejony dzisiejszej Luizjany były odwiedzane przez hiszpańskich poszukiwaczy El Dorado w pierwszej połowie XVI wieku , niedługo po odkryciu Ameryki przez Krzysztofa Kolumba .
Po daremnych poszukiwaniach bogactw i rozwiniętych państw indiańskich , Hiszpania nie założyła stałych kolonii w rejonie .
Po podboju dużo bogatszych terytoriów w Meksyku zagospodarowanych przez stosunkowo wysoko rozwiniętą cywilizację Azteków , Hiszpanie nie widzieli potrzeby osiedlenia się na dziewiczym terytorium nad północnymi wybrzeżami Zatoki Meksykańskiej .
Rejon pozostawał w dziewiczym stanie przez następne 200 lat , aż do końca XVII wieku , kiedy zainteresowała się nim Francja .
W 1682 Robert de La Salle dotarł od północy do ujścia rzeki Missisipi i prawem odkrywcy objął ogromne tereny przylegające do niej we władanie Francji .
Pierwszą stałą francuską kolonię założył w 1699 Pierre Le Moyne d ’ Iberville .
