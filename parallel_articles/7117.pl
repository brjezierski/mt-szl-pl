Lukas Podolski
Lukas Josef Podolski ( urodzony jako `` Łukasz Józef Podolski '' ; ur .
4 czerwca 1985 w Gliwicach ) – niemiecki piłkarz polskiego pochodzenia , występujący na pozycji napastnika w polskim klubie Górnik Zabrze .
W latach 2004–2017 reprezentant Niemiec .
Mistrz Świata z 2014 , dwukrotny brązowy medalista mistrzostw Świata ( 2006 , 2010 ) , srebrny medalista mistrzostw Europy z 2008 , a także brązowy medalista Pucharu Konfederacji 2005 .
Ze 130 występami zajmuje trzecie miejsce pod względem liczby występów w reprezentacji Niemiec , jako zdobywca 49 goli jest trzecim najlepszym strzelcem w historii niemieckiej drużyny narodowej .
Profesjonalną karierę rozpoczął w barwach niemieckiego 1 .
FC Köln , gdzie trafił w 1993 .
W pierwszym zespole zadebiutował w 2003 , jako osiemnastolatek i w ciągu trzech sezonów rozegrał dla klubu 85 meczów strzelając 51 goli .
W 2006 trafił do Bayernu Monachium , z którym w 2008 sięgnął po krajowy dublet , mistrzostwo Niemiec oraz Puchar Niemiec .
W 2009 powrócił do 1 .
FC Köln , aby trzy lata później dołączyć do Arsenalu , z którym w 2014 zdobył Puchar Anglii .
W 2015 w ramach wypożyczenia występował we włoskim Interze Mediolan .
Następnie trafił do tureckiego Galatasaray SK , gdzie w 2016 zdobył Puchar Turcji .
W latach 2017–2019 występował w japońskim Vissel Kobe , natomiast w sezonie 2020/2021 w tureckim Antalyasporze .
W 2021 został zawodnikiem Górnika Zabrze .
Życie prywatne .
Zainteresowanie futbolem zaszczepił w młodym Łukaszu jego ojciec Waldemar Podolski , który w przeszłości również grał zawodowo w piłkę nożną w takich klubach jak Górnik Knurów , ROW Rybnik i Szombierki Bytom ( w 1980 roku zdobył z tą drużyną mistrzostwo Polski ) .
Jego matka grała w piłkę ręczną w klubie Sośnica Gliwice .
Do Niemiec przeprowadził się z rodzicami w 1987 .
Pomimo że od drugiego roku życia mieszka w Niemczech , zachował silne więzi z Polską ( szczególnie z Górnym Śląskiem , gdzie mieszka jego rodzina i z którą utrzymuje kontakt ) .
11 czerwca 2011 w kościele parafialnym w Kamionnie zawarł związek małżeński z Moniką Puchalską .
Na ceremonii obecny był syn Podolskich – Louis ( ur .
14 kwietnia 2008 ) .
6 czerwca 2016 urodziło się drugie dziecko Podolskich – córka Maya .
20 stycznia 2023 urodziło się trzecie dziecko Podolskich - córka Ella .
Ma wykształcenie średnie zawodowe .
