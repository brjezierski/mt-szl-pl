Luksor
Luksor ( arab .
الأقصر , wym .
egip . )
– miasto w południowym Egipcie , na prawym brzegu Nilu , w miejscu południowej części starożytnych Teb , oddzielone kanałem od ich części północnej , stanowiącej obecnie miasto Karnak .
W starożytności zwane egip .
`` Ipet-resejet – „ Harem Południowy ” '' .
Nazwa miasta oznacza dosłownie : „ miasto pałaców ” .
Ponoć arabscy najeźdźcy z VII wieku , widząc wszystkie świątynie mieli pomyśleć , że są one pałacami królewskimi stąd miałaby wywodzić się nazwa .
Luksor jest jednym z najpopularniejszych turystycznych miast w Egipcie .
Atrakcje turystyczne Luksoru .
Miasto to nazywane jest często największym muzeum świata na świeżym powietrzu ze względu na ruiny kompleksu świątyń w Karnaku , świątynię Luksoru oraz niezliczoną liczbę starożytnych posągów , świątyń i krypt ( m.in .
Dolinę Królów ) na zachodnim brzegu Nilu .
Z tego powodu Luksor jest doskonałą bazą turystyczną i popularnym miejscem na spędzenie wakacji , zarówno jako miasto , jak i jako start ( lub meta ) rejsów po Nilu .
W 1979 Luksor został wpisany na Listę światowego dziedzictwa UNESCO .
Atak terrorystyczny w 1997 roku .
17 listopada 1997 roku bojówki muzułmańskie Bractwa Muzułmańskiego dokonały ataku , zabijając 58 zagranicznych turystów oraz 4 Egipcjan przed wejściem do świątyni Hatszepsut .
Policja zlikwidowała zamachowców , lecz uważa się , że byli oni wspierani przez terrorystę pochodzenia saudyjskiego , Osamę bin Ladena .
