Luleå ( stacja kolejowa )
Luleå ( `` Luleå Centralstation '' ) – stacja kolejowa w Luleå , w regionie Norrbotten , w Szwecji .
Znajdują się tu 3 perony .
Stacja znajduje się na linii Stambanan genom övre Norrland ( główna linia przez górny Norrland ) .
Obsługuje połączenia kolejowe m.in .
z Kiruną przez linię Malmbanan , Narwikiem przez linię Ofotbanen , Umeå oraz Sztokholmem .
Luleå ma być stacją końcową planowanej linii Norrbotniabanan , która ma prowadzić na południe do Umeå i w ten sposób , łącząc się z nowo otwartą Botniabanan , umożliwiać szybkie połączenia ze Sztokholmem , Robertsfors , Skellefteå czy Piteå .
