Lunner ( przystanek kolejowy )
Lunner – kolejowy przystanek osobowy w Lunner , w regionie Oppland w Norwegii , jest oddalony od Oslo Sentralstasjon o 61,13 km .
Jest położony na wysokości 280,9 m n.p.m .
Ruch pasażerski .
Leży na linii Gjøvikbanen .
Jest elementem kolei aglomeracyjnej w Oslo - w systemie SKM ma numer 300 .
Obsługuje Oslo Sentralstasjon , Gjøvik .
Pociągi odjeżdżają w obu kierunkach co pół godziny ; nie wszystkie pociągi zatrzymują się na wszystkich stacjach .
Obsługa pasażerów .
Poczekalnia , wiata , parking na 50 miejsc , przystanek autobusowy .
Odprawa podróżnych odbywa się w pociągu .
