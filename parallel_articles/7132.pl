Lusaka
Lusaka – stolica i największe miasto Zambii .
Jest położona w południowo-środkowej części kraju na wysokości 1300 m. Miasto jest centrum handlowym i siedzibą rządu .
W mieście znajduje się największy w Zambii uniwersytet .
Lusaka została założona w roku 1905 przez osadników z Europy .
W maju 1935 r. zastąpiła Livingstone jako stolica kolonii brytyjskiej Rodezji Północnej .
W 1964 roku Lusaka została stolicą niepodległej Zambii .
W chwili uzyskania niepodległości w 1964 roku ludność Lusaki liczyła nieco ponad 100 tys. , czyli mniej niż 4 % populacji kraju .
Do 2011 roku Lusaka była domem dla ponad 1,8 mln ludzi – co stanowiło 10 % całej populacji Zambii .
