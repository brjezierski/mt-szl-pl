Lutownica
Lutownica to narzędzie służące do lutowania .
Składa się ona z kolby , służącej jako uchwyt oraz grota , czyli części mającej bezpośredni styk ze spoiwem – lutem .
Możemy je podzielić na :
Współcześnie lutownic elektrycznych używa się niemal wyłącznie do prac z obwodami elektronicznymi o niewielkich wymiarach , zatem moc elektryczna grzejników nie jest wysoka , nie przekracza na ogół kilkudziesięciu watów ( spotykane najczęściej mieszczą się w przedziale od 40 do 125 W ) .
Znacznie większe lutownice elektryczne ( zazwyczaj kolbowe ) , o mocy sięgającej 400 - 500 watów stosowane bywają do lutowania blach ( np .
rynien miedzianych lub stalowych ocynkowanych ) .
Mają one – oprócz dużej mocy – także stosunkowo duże wymiary grota , zapewniające dużą bezwładność cieplną , korzystną przy pracy z elementami o dużej powierzchni .
Lutownice kolbowe do blach i rur wypierane jednak są coraz częściej lutownicami gazowymi mającymi m.in .
