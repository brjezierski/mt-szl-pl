Luwr
Luwr ( fr .
`` Louvre '' , `` Musée du Louvre '' [ ] ) – dawny pałac królewski w Paryżu , obecnie muzeum sztuki .
Jedno z największych muzeów na świecie , jest też najczęściej odwiedzaną placówką tego typu na świecie .
Stanowi jeden z ważniejszych punktów orientacyjnych stolicy Francji .
Luwr położony jest między rue de Rivoli i prawym brzegiem Sekwany oraz ogrodami Tuileries i rue du Louvre w obrębie 1. dzielnicy .
W kompleksie budynków o całkowitej powierzchni wynoszącej 60 600 metrów kwadratowych znajdują się zbiory liczące około 350 000 dzieł sztuki od czasów najdawniejszych po połowę wieku XIX , dzieła światowego dziedzictwa o największej sławie takie jak np .
stela z kodeksem Hammurabiego , Nike z Samotraki , Wenus z Milo , Mona Lisa Leonarda da Vinci .
Muzeum znajduje się w Pałacu Luwru ( Palais du Louvre ) znanym już w średniowieczu .
U schyłku XII wieku , król Filip II August wzniósł w tym miejscu zamek warowny .
W drugiej połowie XIV wieku Karol V Mądry uczynił z zamku rezydencję królewską .
W XVI wieku Franciszek I zamienił tę budowlę w monumentalny renesansowy pałac .
Autorem barokowej przebudowy pałacu był Jacques Lemercier .
W 1672 Ludwik XIV przeprowadził się do Wersalu , natomiast w Luwrze zostawił bogatą kolekcję dzieł sztuki antycznej .
W 1692 gmach został siedzibą `` Académie des Inscriptions et Belles Lettres '' oraz `` Académie Royale de Peinture et de Sculpture '' , w roku 1699 Akademia zorganizowała pierwszy Salon wystawowy .
Podczas rewolucji francuskiej 8 września 1793 r. Konwent Narodowy wydał dekret o zamienieniu pałacu w muzeum .
Muzeum zostało otwarte 10 sierpnia 1793 r. Ekspozycja liczyła 537 obrazów .
Lwią część stanowiły konfiskaty dóbr kościelnych i królewskich .
Ze względu na problemy związane ze stanem budynku muzeum zamknięto na lata 1796–1801 .
Kampania napoleońska wpłynęła na duży wzrost kolekcji , przy czym większość stanowiły łupy ze zbiorów na terenach podbitych przez Napoleona I Bonaparte , po porażce wojsk dyktatora pod Waterloo dzieła zwrócono prawowitym właścicielom .
Za czasów panowania Ludwika XVIII i Karola X oraz Drugiego Cesarstwa nastąpił wzrost kolekcji , która przekroczyła 20 tysięcy dzieł .
Kolejny wzrost kolekcji to owoc licznych nabytków muzeum i darów prywatnych kolekcjonerów dzieł sztuki .
W 2008 roku zbiory Luwru podzielono na osiem kategorii , każdej z nich przypada odrębny dział .
Są to Działy : Starożytnego Egiptu , Starożytnego Bliskiego Wschodu , Starożytnej Grecji , Starożytnego Rzymu , Sztuki Islamu , Rzeźby , Malarstwa , Rzemiosła Artystycznego , Rysunku i Grafiki .
Położenie .
Luwr to jedno z najstarszych i największych muzeów na świecie .
Położony w centrum historycznym Paryża – między prawym brzegiem Sekwany a ulicą Rivoli oraz ogrodami Tuilerie a kościołem Saint Germain l ’ Auxerrois .
Luwr położony jest na jednej linii z Champs-Élysées , wchodząc w skład tzw .
osi historycznej Paryża .
Po zniszczeniu podczas Komuny Paryskiej , a następnie zburzeniu Pałacu Tuileries , oś uległa wydłużeniu .
