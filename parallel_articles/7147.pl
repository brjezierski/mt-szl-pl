Czasznica biała
Czasznica biała ( `` Calvatia candida '' ( Rostk . )
Hollós ) – gatunek grzybów należący do rodziny purchawkowatych ( `` Lycoperdaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Calvatia , Lycoperdaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy takson ten zdiagnozował w roku 1839 Friedrich Wilhelm Rostkovius nadając mu nazwę `` Langermannia candida '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1902 roku László Hollós przenosząc go do rodzaju `` Calvatia '' .
Synonimy :
Nazwę polską nadała Wanda Rudnicka-Jezierska w 1991 r .
Morfologia .
Nieduży , o średnicy 1,5–5 cm , wyjątkowo do 7 cm , mniej więcej kulisty , lub nieco spłaszczony .
Wyrasta z grubego ( ok. 1 cm ) i zazwyczaj rozgałęzionego sznura grzybniowego .
Powierzchnia ( egzoperydium ) gładka lub nieco oprószona , początkowo biała , podczas dojrzewania owocnika coraz ciemniejsza , w końcu brązowa .
Egzoperydium podczas dojrzewania owocnika twardnieje i pęka na duże płaty oddzielające się od wewnętrznej warstwy endoperydium .
To ostatnie ma barwę od żółtawej do szarobrązowej , jest matowe , gładkie i bardzo kruche .
