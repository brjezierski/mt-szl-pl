Administracyjne .
Lębork leży w północnej Polsce , w północnej części województwa pomorskiego , centralnej części powiatu lęborskiego .
Miasto graniczy z dwiema gminami wiejskimi – od zachodu , północy , wschodu , a także częściowo od południa z gminą Nowa Wieś Lęborska oraz na niewielkim odcinku południowym z gminą Cewice .
