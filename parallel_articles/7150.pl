Lębork
Lębork ( pierwotnie `` Lewino '' lub `` Łebno '' ; kaszb .
`` Lãbórg '' lub też `` Lãbórch '' , niem .
`` Lauenburg in Pommern '' ) – miasto w województwie pomorskim , siedziba powiatu lęborskiego .
Lębork uzyskał lokację miejską w 1341 roku .
W latach 1975–1998 miasto administracyjnie należało do województwa słupskiego .
Lębork leży w pradolinie Redy-Łeby , nad Łebą i jej dopływem Okalicą , na północnym krańcu Puszczy Kaszubskiej , w bliskim sąsiedztwie Jeziora Lubowidzkiego na wschodzie , około 80 km na zachód od Gdańska .
Lębork leży na północy Kaszub .
Ośrodek przemysłu elektromaszynowego , spożywczego , mineralnego ( cegielnia Wienerberger ) oraz lekkiego .
Warte zobaczenia są mury miejskie wraz z wieżami , kościół pw .
św .
Jakuba , miejski ratusz , budynek sądu ( dawny zamek krzyżacki ) oraz muzeum .
Według danych GUS z 30 czerwca 2021 r. , Lębork liczył 34 977 mieszkańców i był pod względem liczby ludności dwunastym miastem w województwie pomorskim .
