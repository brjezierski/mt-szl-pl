Lędyczek
Lędyczek ( niem .
`` Landeck '' ) – wieś ( dawniej miasto ) w Polsce położona w województwie wielkopolskim , w powiecie złotowskim , w gminie Okonek , ośrodek turystyczny .
Lędyczek uzyskał lokację miejską w 1809 roku , zdegradowany 1 stycznia 1973 .
Wieś królewska starostwa człuchowskiego w województwie pomorskim w drugiej połowie XVI wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa pilskiego .
Położenie .
Lędyczek leży w Dolinie Gwdy , na lewym ( wschodnim ) brzegu Gwdy oraz nad jej lewym dopływem Debrzynką .
Ponadto przy północnej części Lędyczka do Gwdy uchodzi rzeka Szczyra , a w pobliżu na zachodnim brzegu do Gwdy uchodzi Czarna .
Przez miejscowość przebiega droga krajowa nr 22 ( Kostrzyn–Grzechotki ) oraz trasy lokalne do Złotowa , Okonka i Łomczewa .
