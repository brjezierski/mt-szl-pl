Lynwood ( Kalifornia )
Lynwood – miasto w Stanach Zjednoczonych , w stanie Kalifornia , w hrabstwie Los Angeles .
W 2010 zamieszkiwało je 69 772 osób .
Miasto leży na wysokości 28 metrów n.p.m .
i zajmuje powierzchnię 12,536 km² .
Prawa miejskie uzyskało 16 lipca 1921 .
Ludzie związani z Lynwood .
W Lynwood urodził się Kevin Costner , amerykański aktor i reżyser filmowy .
