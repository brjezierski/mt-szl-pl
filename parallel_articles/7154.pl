Lyon ( rzeka )
Lyon ( ang .
`` River Lyon '' ) – rzeka w środkowej Szkocji , w hrabstwie Perth and Kinross , dopływ rzeki Tay .
Rzeka wypływa z jeziora Loch Lyon i płynie w kierunku wschodnim , doliną Glen Lyon .
Nad rzeką położone są osady Pubil , Cashlie , Bridge of Balgie , Innerwick , Camusvrachan , Inervar , Croftgarrow , Fortingall i Keltneyburn .
Za tą ostatnią rzeka uchodzi do Tay .
