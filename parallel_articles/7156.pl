Kępkowiec czerniejący
Kępkowiec czerniejący ( `` Calocybe gangraenosa '' ( Fr . )
V .
Hofst. , Moncalvo , Redhead & amp ; Vilgalys ) – gatunek grzybów z rodziny kępkowcowatych ( `` Lyophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Calocybe , Lyophyllaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1838 r. Elias Fries nadając mu nazwę `` Agaricus gangraenosus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2012 r. Valérie Hofstetter , Jean-Marc Moncalvo , Scott Alan Redhead i Rytas J. Vilgalys przenosząc go do rodzaju `` Calocybe '' .
Synonimy :
W 2003 r. Władysław Wojewoda zaproponował nazwę polską kępkowiec czerniejący ( wówczas gatunek ten zaliczany był do rodzaju `` Lyophyllum '' czyli kępkowiec ) .
Po przeniesieniu do rodzaju `` Calocybe '' stała się ona niespójna z nazwą naukową .
Występowanie i siedlisko .
Znane jest występowanie tego gatunku na nielicznych stanowiskach w Ameryce Północnej i na licznych w Europie .
W. Wojewoda w zestawieniu wielkoowocnikowych grzybów podstawkowych Polski w 2003 r. przytacza 2 stanowiska z uwagą , że jego rozprzestrzenienie i stopień zagrożenia nie są znane .
Grzyb saprotroficzny .
Okazy znalezione w Polsce rosły na ziemi , w trawie na polanach .
