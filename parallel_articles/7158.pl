Lysurus mokusin
Lysurus mokusin ( L. ) Fr .
– gatunek grzybów z rodziny sromotnikowatych ( `` Phallaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lysurus , Phallaceae , Phallales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1753 r. Karol Linneusz nadając mu nazwę `` Phallus mokusin '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1823 r. Elias Fries , przenosząc go do rodzaju `` Lysurus '' .
Morfologia .
Niedojrzałe owocniki są białe , jajowate , o gąbczastej konsystencji , średnicy 1-3 cm .
Są przytwierdzone do podłoża pasmami grzybni określanymi jako ryzomorfy .
Dojrzewający owocnik pęka i bardzo szybko się rozrasta , a pozostałości osłony owocnika w postaci pochwy pozostają przy podstawie korzenia .
Trzon grzyba jest pusty w środku , gąbczasty , o wymiarach 10–15 cm x 1,5–2,5 cm , o barwie od białej , różowej lub czerwonej .
