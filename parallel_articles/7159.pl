Lytton ( Kanada )
Lytton – miejscowość ( `` village '' ) w Kanadzie , w prowincji Kolumbia Brytyjska , w Kamloops Division Yale Land District , podjednostka podziału statystycznego , położona u zbiegu rzek Fraser i Thompson River , na północny wschód od Vancouver .
Według spisu powszechnego z 2016 obszar village to : 6,73 km² , a zamieszkiwało wówczas ten obszar 249 osób ( gęstość zaludnienia : 37 os./km² ) .
Historia .
Pierwsze ślady zasiedlenia terenów dzisiejszego Lytton przez tubylcze ludy Kanady odnotowano ok. 10 tysięcy lat temu .
Miejscowość została nazwana w 1858 na cześć Edwarda Bulwera-Lyttona przez gubernatora Jamesa Douglasa , w 1945 uzyskała status village .
