László Sólyom
László Sólyom ( wym .
; ur .
3 stycznia 1942 w Peczu ) – węgierski prawnik , nauczyciel akademicki i polityk , przewodniczący Trybunału Konstytucyjnego ( 1990–1998 ) .
W latach 2005–2010 prezydent Węgier .
Życiorys .
W 1965 ukończył prawo na Uniwersytecie w Peczu , w tym samym roku uzyskał uprawnienia bibliotekarza w Państwowej Biblioteka Széchényiego .
Kształcił się następnie na Uniwersytecie Friedricha Schillera w Jenie , na którym pracował w latach 1966–1969 .
Do połowy lat 70. był zatrudniony w parlamentarnej bibliotece .
Od 1969 pozostawał pracownikiem naukowym instytutu nauk politycznych i prawnych Węgierskiej Akademii Nauk , doktoryzował się w tym instytucie w 1981 .
Od 1978 wykładowca węgierskich uczelni , w tym w latach 1983–1998 profesor w katedrze prawa cywilnego Uniwersytetu im .
Loránda Eötvösa w Budapeszcie , a od 1995 profesor na Katolickim Uniwersytecie Pétera Pázmánya .
W 1999 otrzymał tytuł doctora honoris causa Uniwersytetu w Kolonii , od 1999 do 2000 gościnnie wykładał na tej uczelni .
W 2001 został członkiem korespondentem , a w 2013 członkiem zwyczajnym Węgierskiej Akademii Nauk .
Pod koniec lat 80. zaangażował się w działalność niezależnych organizacji obywatelskich , m.in .
zrzeszającego prawników Független Jogász Fórum .
Był też jednym z założycieli Węgierskiego Forum Demokratycznego i krótko członkiem władz wykonawczych tej partii .
Reprezentował ją w trakcie negocjacji z innymi ugrupowaniami opozycyjnymi .
Jeszcze w 1989 wybrany w skład Trybunału Konstytucyjnego .
Został wiceprzewodniczącym tej instytucji , a od 1990 do końca kadencji w 1998 zajmował stanowisko jej prezesa .
W 2005 organizacja społeczna Védegylet zgłosiła jego kandydaturę na urząd prezydenta , wybieranego przez Zgromadzenia Narodowego .
Poparcie zadeklarowali posłowie opozycyjnych ugrupowań – Fideszu i Węgierskiego Forum Demokratycznego .
7 czerwca 2005 László Sólyom został wybrany na ten urząd w trzeciej turze głosowania , otrzymując 185 głosów , podczas gdy kandydatka rządzących socjalistów Katalin Szili dostała 182 głosy .
Wybór umożliwiło mu wstrzymanie się od głosu przez deputowanych współrządzącego Związku Wolnych Demokratów .
Urząd objął 5 sierpnia 2005 i sprawował go przez pełną kadencję do 6 sierpnia 2010 , kiedy to zastąpił go Pál Schmitt .
Odznaczony m.in .
Orderem Orła Białego ( 2009 ) .
