Lévis
Lévis – miasto w kanadyjskiej prowincji Quebec , stolica regionu Chaudière-Appalaches .
Położone jest w aglomeracji miasta Québec , na przeciwległym brzegu Rzeki Świętego Wawrzyńca .
Od 1919 roku ma z nim połączenie mostowe ( Pont de Québec ) .
Liczba mieszkańców Lévis wynosiła w 2011 roku 138 769 osób .
Język francuski jest językiem ojczystym dla 97,4 % , angielski dla 1,1 % mieszkańców ( 2016 ) .
