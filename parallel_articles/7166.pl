Løten ( stacja kolejowa )
Løten – stacja kolejowa w Løten , w regionie Hedmark w Norwegii , jest oddalona od Oslo Sentralstasjon o 143,78 km .
Położona na wysokości 231,7 m n.p.m .
Ruch pasażerski .
Leży na linii Rørosbanen , jednej z dwóch równoległych linii kolejowych prowadzących z Oslo do Trondheim .
Stacja obsługuje ruch dalekobieżny do Hamar , Røros oraz jedno połączenie dziennie do Trondheim S .
Obsługa pasażerów .
Poczekalnia , wiata , kasa biletowa , telefon publiczny , parking na 25 miejsc , parking rowerowy , kawiarnia , sklepik , ułatwienia dla niepełnosprawnych , schowki bagażowe , kiosk , przystanek autobusowy , postój taksówek .
