Lubeka
Lubeka ( ; poł .
`` Ljubice '' ; dolnoniem .
`` Lübeck '' , `` Lübeek '' ) – miasto na prawach powiatu ( niem .
`` kreisfreie Stadt '' ) w północnych Niemczech , w kraju związkowym Szlezwik-Holsztyn .
Leży nad Zatoką Lubecką , przy ujściu do niej rzeki Trave .
Jest jednym z większych portów morskich w Niemczech , a dawniej była głównym miastem Ligi Hanzeatyckiej .
Jedna z dzielnic Lubeki – Travemünde – jest popularnym ośrodkiem wypoczynkowym i portem nad wybrzeżem Bałtyku .
Teren w okolicach Lubeki został najpóźniej w VIII wieku zaludniony przez Słowian , którzy osiedlili się na brzegach Trave kilka kilometrów od dzisiejszego centrum miasta .
Zostali oni podbici w 1158 przez Henryka Lwa , który wkrótce w tym miejscu założył miasto .
W XIV wieku Lubeka stała się „ Królową Hanzy ” , będąc zdecydowanie największym i najsilniejszym członkiem tej średniowiecznej organizacji handlowej .
W 1533 roku popadła w konflikt zbrojny z Danią , co doprowadziło do utraty jej potęgi .
