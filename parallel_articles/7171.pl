Śliskość .
Do niedawna śliskość lodu przypisywano malejącej temperaturze jego topnienia pod wpływem ciśnienia , powstałego w wyniku nacisku na powierzchnię lodu , co miało sprawiać jego topnienie na powierzchni i powstanie płynnej warstwy poślizgowej .
Łatwo wykazać fałszywość lub niepełność tej koncepcji , gdyż spadek temperatury topnienia przy wzroście ciśnienia jest niewielki i już w temperaturze kilku stopni poniżej zera lód nie powinien być śliski .
Nadal nie jest całkowicie jasne , dlaczego lód jest śliski , choć wiadomo , że cząsteczki lodu znajdujące się na jego wierzchniej są słabiej związane z pozostałymi cząsteczkami , prawdopodobnie na skutek oddziaływania z cząsteczkami powietrza .
