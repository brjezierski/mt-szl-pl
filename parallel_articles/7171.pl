Lód
Lód – minerał z gromady tlenków o wzorze chemicznym H2O .
Jest to stan stały wody .
Właściwości fizyczno-chemiczne .
Zamarzanie czystej wody , przy ciśnieniu atmosferycznym wynoszącym 101 325 Pa , zachodzi w temperaturze 0 °C .
Przy zamarzaniu objętość lodu jest większa niż tworzącej go wody , przez co gęstość lodu jest o blisko 10 % mniejsza ( w temperaturze 0 °C gęstość wody wynosi 0,9998 g/cm³ , a lodu – 0,9167 g/cm³ ) .
Dzięki temu lód pływa na wodzie , a zbiorniki wodne zamarzają od powierzchni w dół .
Lód jest kryształem dwójłomnym , jednak jego współczynniki załamania różnią się niewiele : wynoszą `` n '' o = 1,309 i `` n '' e = 1,313 dla fali o długości około 590 nm .
Odmiany polimorficzne lodu .
W dużym zakresie ciśnień można wyróżnić co najmniej osiemnaście odmian polimorficznych lodu , oznaczanych symbolicznie cyframi rzymskimi .
