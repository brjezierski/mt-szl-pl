M ’ Baye Niang
M ’ Baye Babacar Niang ( ur .
19 grudnia 1994 w Meulan-en-Yvelines ) – senegalski piłkarz francuskiego pochodzenia występujący na pozycji napastnika we klubie Stade Rennais oraz w reprezentacji Senegalu .
Kariera piłkarska .
Niang urodził się we Francji w rodzinie pochodzenia senegalskiego .
Swoją karierę piłkarską rozpoczął w klubie Basse-Seine Les Mureaux .
W 2003 roku podjął treningi w AS Poissy , a w 2007 roku przeszedł do SM Caen .
W lutym 2011 podpisał profesjonalny kontrakt z tym klubem .
24 kwietnia 2011 zadebiutował w Ligue 1 w zremisowanym 1:1 domowym meczu z Toulouse FC , gdy w 77. minucie zmienił Kandię Traoré .
7 maja 2011 w zremisowanym 1:1 domowym spotkaniu z RC Lens strzelił swojego pierwszego gola w barwach Caen .
Stał się tym samym najmłodszym strzelcem gola w historii tego klubu .
W sezonie 2010/2011 zdobył łącznie trzy bramki , a w sezonie 2011/2012 dwukrotnie wpisywał się na listę strzelców .
W sezonie 2011/2012 Caen spadło do Ligue 2 .
W sierpniu 2012 roku Niang podpisał trzyletni kontrakt z zespołem Milanu .
Swój debiut w Serie A zanotował 1 września 2012 w wygranym 3:1 wyjazdowym meczu z Bologną .
W 91. minucie tego meczu zmienił Giampaolo Pazziniego .
13 grudnia strzelił swojego pierwszego gola w barwach Milanu , w spotkaniu Pucharu Włoch z Regginą .
Kariera reprezentacyjna .
Niang grał w reprezentacjach Francji U-16 i U-17 , a w 2011 roku zadebiutował w kadrze U-21 .
Na Mistrzostwach Świata 2018 strzelił gola w wygranym 2:1 meczu z Polską .
