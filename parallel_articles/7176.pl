MS-DOS
MS-DOS ( ) – system operacyjny stworzony przez przedsiębiorstwo Microsoft na podstawie nabytego przez nie kodu źródłowego systemu QDOS ( ) stworzonego przez Seattle Computers .
Jest to wersja systemu operacyjnego DOS przeznaczona dla komputerów IBM PC .
Choć MS-DOS sam w sobie jest środowiskiem tekstowym , rolę graficznej nakładki działającej na tym systemie spełniać miał DOS Shell oraz wczesne wersje systemu Microsoft Windows .
Od rozwiązań Microsoftu bardziej popularny okazał się jednak Norton Commander – napisany poza firmą Microsoft DOS-owy menedżer plików , korzystający z tzw .
semigrafiki środowiska tekstowego DOS .
Prócz tego istniały także inne programy będące graficznymi nakładkami na system , takie jak np .
1dir i XTree .
Późniejsze wersje Windows ( od 3.0 wzwyż w linii systemów 9x , kończącej się systemem Millennium Edition ) stawały się coraz bardziej samodzielnymi systemami operacyjnymi , tworząc odrębną , własną platformę programistyczną .
