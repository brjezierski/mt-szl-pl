Maassluis
Maassluis – miasto w zachodniej Holandii , w prowincji Holandia Południowa , zamieszkiwana przez ok. 32 tys .
osób .
Historia .
Maassluis zostało wybudowane około 1340 roku jako osada przy śluzie na zaporze wodnej między Morzem Północnym i Rotterdamem .
Początkowo nazywało się Maeslandsluys i było częścią Maesland .
