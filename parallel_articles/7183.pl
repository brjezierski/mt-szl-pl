Jest powszechnie uznawany za wybitnego stratega i jednego z największych zdobywców w historii ludzkości .
Okres panowania Aleksandra wyznacza granicę między dwiema epokami historii starożytnej : okresem klasycznym i epoką hellenistyczną .
Jego ojciec Filip II zreformował państwo macedońskie i zdołał podporządkować sobie większość Grecji .
