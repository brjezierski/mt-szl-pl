Aleksander urodził się 6 dnia ateńskiego miesiąca Hekatombajon , co w naszym kalendarzu odpowiada mniej więcej 19–20 lipca , w 356 r. p.n.e .
Był synem króla Macedonii i twórcy potęgi tego państwa , Filipa II , i Olimpias , córki króla Epiru Neoptolemosa I. Filip miał kilka żon i kochanek , ale Olimpias , z racji powicia uznanego za następcę tronu Aleksandra , miała wśród nich pozycję uprzywilejowaną .
Jak w przypadku większości osób , które w starożytności stały się sławne , narodziny Aleksandra otoczono później różnymi legendami i cudownymi zdarzeniami .
Zgodnie z tradycją synchronizowania dat ważnych wydarzeń przez autorów greckich , podawano , że Aleksander narodził się tego samego dnia , którego spłonęła świątynia Artemidy w Efezie , jeden z siedmiu cudów świata .
Spekulowano , że sama Artemida nie mogła uchronić swojego przybytku przed pożarem , gdyż była zajęta asystowaniem Olimpias przy porodzie .
Odczytywano to jako znak przyszłego podboju Azji przez Aleksandra .
Zarówno Filip , jak i Olimpias mieli doświadczyć tego dnia wizji , które rzekomo zwiastowały wielkie czyny przyszłego króla .
