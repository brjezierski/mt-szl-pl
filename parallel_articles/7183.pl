Aleksander Macedoński
Aleksander III Macedoński ( `` Aleksandros ho Tritos ho Makedon '' ) zwany też Aleksandrem Wielkim ( `` Aleksandros ho Megas '' ) i niezwyciężonym ( άνίκητος ) ( ur .
