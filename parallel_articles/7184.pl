Maceió
Maceió ( wym .
[ ] ) – miasto w Brazylii , stolica stanu Alagoas .
Nazwa miasta pochodzi
od słowa `` bagno '' które w języku plemienia Tupi brzmi Maçaió .
Historia .
Maceió zostało założone w 1815 roku .
Rozwój miasta rozpoczął się wraz z przybyciem statków biorących drewno z zatoki Jaraguá .
Dzięki dalszemu rozwojowi , Maceió stał się stolicą stanu Alagoas w 9 grudnia 1839 roku .
