Macerata ( stacja kolejowa )
Macerata ( wł .
`` Stazione di Macerata '' ) – stacja kolejowa w Maceracie , w regionie Marche , we Włoszech .
Znajdują się tu 3 perony i budynek stacyjny , w którym znajdują się kasy biletowe , poczekalnia dla podróżnych , toalety , kiosk i bar dworcowy , przy dworcu parking samochodowy .
Stacja przyjmuje rocznie około 900000 pasażerów .
Zatrzymują się na niej 43 pociągi dziennie ; połączenia z Ankoną , Ascoli Piceno , Civitanovą Marche oraz Fabriano .
