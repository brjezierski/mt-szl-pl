Machaczkała
Machaczkała ( ; `` Maħačqala '' ) – stolica Dagestanu , autonomicznej republiki , wchodzącej w skład Federacji Rosyjskiej .
Machaczkała leży nad Morzem Kaspijskim , w nizinnej części Dagestanu .
Przez miasto przebiegają ważne trasy transportowe z Rosji do Azerbejdżanu i Iranu .
Machaczkała jest także ważnym portem Morza Kaspijskiego .
Historia .
Machaczkała została założona w 1844 jako twierdza Pietrowskoje .
W 1919 w Machaczkale funkcjonowała polska placówka o charakterze konsularnym .
Dzisiejszą nazwę miasto nosi od 1921 .
