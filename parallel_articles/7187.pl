Machida
– miasto w Japonii , na wyspie Honsiu , w aglomeracji Tokio .
Ma powierzchnię 71,55 km2 .
W 2020 r. mieszkało w nim 431 525 osób , w 191 959 gospodarstwach domowych ( w 2010 r. 426 827 osób , w 179 937 gospodarstwach domowych ) .
Miasto powstało w 1958 roku poprzez połączenie miasta Hara-Machida z trzema okolicznymi wioskami .
W 1908 roku przez miasto poprowadzono linię kolejową łączącą Jokohamę z Hachiōji , zwiększając jego znaczenie jako ośrodka handlu i transportu .
W latach 1960–1970 zanotowano znaczny , niemal trzykrotny wzrost liczby ludności , który zmalał w latach 70 .
XX wieku .
W mieście rozwinął się przemysł maszynowy , elektroniczny oraz spożywczy .
