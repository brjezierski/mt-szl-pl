Maciej Kozłowski ( dyplomata )
Maciej Kozłowski ( ur .
12 stycznia 1943 w Luborzycy ) – polski historyk , dziennikarz , publicysta , działacz opozycji w okresie PRL , dyplomata , w latach 1999–2003 ambasador RP w Izraelu .
Życiorys .
Do 1989 .
W 1966 ukończył studia na Wydziale Filozoficzno-Historycznym Uniwersytetu Jagiellońskiego ( archeologia śródziemnomorska ) .
W latach 1966–1968 uczęszczał na studia podyplomowe dziennikarskie na Uniwersytecie Warszawskim .
Równocześnie aktywnie uprawiał wspinaczkę górską .
Po szeregu wejść w Tatrach w sierpniu 1965 r. wraz z Maciejem Włodkiem wspinał się w masywie Olimpu .
W 1966 r. wziął udział w IV Polskiej Wyprawie w Hindukusz afgański , podczas której dokonał m.in .
17 sierpnia z Jerzym Potockim i Belgiem Jeanem Bourgeois pierwszego wejścia na Sad Istrāgh ( 5859 m n.p.m .
) , a 30 sierpnia uczestniczył wraz z Jeanem Bourgeois , Andrzejem Heinrichem , Andrzejem Mrozem , Jerzym Potockim i Jackiem Porębą w czwartym z kolei ( drugim polskim ) wejściu na Noszak ( 7492 m n.p.m . ) .
