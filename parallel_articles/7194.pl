Czubajka sutkowata
Czubajka sutkowata ( `` Macrolepiota mastoidea '' ( Fr . )
Singer ) – gatunek grzybów należący do rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Macrolepiota , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy takson ten zdiagnozował w 1821 r. Elias Fries nadając mu nazwę `` Agaricus mastoideus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1951 Rolf Singer , przenosząc go do rodzaju `` Macrolepiota '' .
Ma 22 synonimy łacińskie .
Niektóre z nich :
Nazwę polską podał Władysław Wojewoda w 1998 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako czubajka właściwa , czubajka wysmukła , stroszka wysmukła , kożuszek .
W niektórych atlasach grzybów jest opisywany jako czubajka beżowa .
Morfologia .
Średnicy od 5 do 12 cm , młody – jajowaty , później wypukły do rozpostartego , jasnobrązowy , jasnomięsnoochrowy , skórka początkowo gwiaździście popękana , u dojrzałych owocników pozostaje taka tylko pośrodku , gdzie jest także ciemniejsza , w innych miejscach delikatnie gęsto , promieniście i ziarnisto łuseczkowata , przy czym między łuseczkami prześwituje bledsze tło .
Przy trzonie wolne , białe .
Wysokości od 10 do 15 cm , średnicy od 0,8 do 1,5 cm , cylindryczny , z bulwiastą podstawą ( do 2,5 cm ) , daje się wyłamać , gładki lub lekko pasemkowaty , łykowaty , rurkowaty , koloru jasnego , brązowawego drewna .
