Madera ( wyspa )
Madera – wyspa należąca do Portugalii , największa i najbardziej zaludniona w archipelagu Madery , o powierzchni 740,7 km² .
W 2011 roku zamieszkiwało ją 267 785 osób , w tym 111 892 osoby w Funchal , największym mieście wyspy .
Jest szczytem wielkiego wulkanu tarczowego wznoszącego się na wysokość 6 km z dna Atlantyku .
Długość wyspy wynosi 57 km , w najszerszym punkcie ma 22 km , długość wybrzeża to 150 km .
Pasmo górskie biegnie przez środek wyspy , sięgając 1862 metrów .
