Madonna ( album )
Madonna – debiutancki album studyjny amerykańskiej piosenkarki popowej Madonny , wydany 27 lipca 1983 roku nakładem wytwórni Sire Records .
W 1985 roku ukazała się jego reedycja , zatytułowana Madonna : The First Album .
Album był nagrywany w 1982 i 1983 roku w Nowym Jorku .
W pierwszej kolejności powstał singel „ Everybody ” , którego produkcję nadzorował Mark Kamins .
Jego klubowy sukces zachęcił przedstawicieli wytwórni do podpisania z Madonną kontraktu na cały album .
Gdy rozpoczęły się prace nad wydawnictwem , produkcję przejął Reggie Lucas .
W wyniku nieporozumień w sprawie brzmienia Madonna zakończyła z nim współpracę , a do końca nagrań nad produkcją czuwał John „ Jellybean ” Benitez .
Ostatecznie na albumie znalazło się osiem utworów , częściowo napisanych przez samą piosenkarkę , a częściowo przez jej współpracowników .
