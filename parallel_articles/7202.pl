Jest stolicą stanu Tamilnadu oraz największym ośrodkiem kulturalnym , ekonomicznym i edukacyjnym południowych Indii .
Jest czwartą aglomeracją miejską w Indiach pod względem ludności .
Ćennaj jest wśród najczęściej odwiedzanych miast Indii , w 2015 był 45. miejscem na świecie pod względem ruchu turystycznego .
Według Wskaźnika Jakości Życia jest najbezpieczniejszym miastem Indii .
Miasto to nazywane jest „ indyjską stolicą zdrowia ” , 45 % ruchu turystycznego to turystyka medyczna .
Z powodu dynamicznie zwiększającej się liczby ludności oraz ogólnego rozwoju gospodarczego Indii Ćennaj mierzy się wieloma problemami ekologicznymi , ekonomicznymi i społecznymi .
Obszar metropolitalny Ćennaj jest największą gospodarką miejską w Indiach i zajmuje trzecie miejsce pod względem wartości PKB na mieszkańca .
Nazywany jest „ Detroit Indii ” z uwagi na skoncentrowanie na tym obszarze ⅓ indyjskiego przemysłu samochodowego .
W mieście rozwija się także przemysł filmowy , a region ten nazywany jest Kollywood .
Ćennaj zostało wybrane do sztandarowego projektu Narendry Modiego , który ma przekształcić 100 miast w smart city .
Historia .
Założone w 1639 r. jako faktoria handlowa przez Brytyjską Kompanię Wschodnioindyjską .
Rok później Brytyjczycy zbudowali Fort Św .
Jerzego , który stał się rdzeniem przyszłego miasta .
W 1746 r. Madras został zdobyty przez wojska francuskie pod dowództwem generała Bertranda Le Bourdonnais'go .
Trzy lata później jednak , Brytyjczycy odzyskali kontrolę nad miastem w wyniku postanowień traktatu pokojowego z Akwizgranu z 1748 r .
22 września 1914 niemiecki krążownik SMS Emden ostrzelał brytyjskie instalacje .
Choć zniszczenia były stosunkowo niewielkie , w mieście wybuchła panika i epizod był ciosem w brytyjskie morale .
W 2000 roku zmieniono nazwę miasta w języku angielskim z Madras na Chennai , a w języku hindi z Madras na Ćennaj .
Nie zmieniano zaś nazwy tamilskiej , która i wcześniej brzmiała Sennaj .
Klimat .
Klimat jest gorący i wilgotny przez większą część roku .
Najgorętszy jest maj i pierwsza część czerwca z temperaturą maksymalną od 38 do 42 °C .
Najzimniejszy jest styczeń z temperaturą minimalną około 18–20 °C .
Rekord najniższej temperatury to 15,8 °C , a najwyższej 45 °C .
Średni roczny opad to 1300 mm , z czego większość przypada na północno-wschodni monsun od połowy września do połowy grudnia .
Cyklony z Zatoki Bengalskiej czasem docierają do miasta .
Najwyższy roczny opad zanotowano w roku 2005 ( 2570 mm ) .
Gospodarka .
Według szacunków PKB ( PSN ) obszaru metropolitalnego Ćennaj wynosi między 58,6 a 66 mld dolarów , co daje 4. lub 6. miejsce wśród miast Indii .
