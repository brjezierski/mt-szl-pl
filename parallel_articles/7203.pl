Madryt ( wspólnota autonomiczna )
Madryt , Wspólnota Madrytu ( ) – jeden z siedemnastu autonomicznych regionów Hiszpanii .
Zajmuje powierzchnię 8028 km² , a jego populacja wynosi około 6,6 mln .
Jego stolicą jest Madryt .
Inne większe miasta to Alcalá de Henares , Móstoles , San Lorenzo de El Escorial i Aranjuez .
Madryt to trzecia co do ludności wspólnota autonomiczna Hiszpanii oraz najgęściej zaludniona .
Jej centrum jest stolica kraju , zaś w całym regionie mieszka ponad sześć milionów osób , z których połowa w samym Madrycie .
W obrębie wspólnoty znajdują się również inne miasta , leżące dookoła madryckiej metropolii , z których dwa przekraczają dwieście tysięcy mieszkańców .
Madryt jest stolicą kraju od 1561 , gdzie została ona przeniesiona z Toledo przez króla Filipa II .
