Madżong
Madżong ( inne spotykane pisownie : `` mahjong '' , `` mah jong '' , `` mahjongg '' ; chin .
trad .
麻將 , chin .
upr .
麻将 , pinyin : májiàng； kant .
麻雀 , jyutping : maa4 zoek3 ; jap .
mājan 麻雀 ) – pochodząca z Chin gra dla czterech graczy .
W grze wykorzystuje się specjalny komplet 144 kamieni podobnych nieco do stosowanych przy grze w domino , ale podzielonych na grupy w sposób podobny do kolorów w kartach i tworzące kilka innych grup – Wiatry , Smoki oraz Kwiaty i Pory Roku .
Gra początkowo była zaplanowana jako hazardowa , gra się też towarzysko .
Istnieją różne wersje zasad z mniejszym albo większym znaczeniem elementu losowego .
W wielu krajach odbywają się turnieje madżonga , w związku z czym powstało bardzo wiele różnych zestawów zasad : kilka typów chińskich , japońskie , brytyjskie , europejskie , amerykańskie i inne .
