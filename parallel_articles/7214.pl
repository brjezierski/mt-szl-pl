Magnitogorsk
Magnitogorsk ( ros .
`` Магнитогорск '' ) – miasto przemysłowe w południowej Rosji , w obwodzie czelabińskim , na Uralu .
Położone w Europie i Azji .
Niedaleko od granicy rosyjsko-kazachskiej .
Historia .
Pierwsza osada na azjatyckim brzegu Uralu została założona w 1743 roku ukazem cesarzowej Elżbiety Romanowej i zasiedlona kozakami orenburskimi .
Lewobrzeżny Magnitogorsk założony został w 1929 roku .
Osada otrzymała prawa miejskie w 1931 roku .
Miasto zostało bardzo rozbudowane podczas pierwszego planu pięcioletniego ( lata 1928–1932 ) .
Gospodarka .
Obecnie miasto jest ważnym ośrodkiem przemysłu metalurgicznego , maszynowego , chemicznego i siedzibą wytwórni fortepianów .
Większość z obiektów przemysłowych ulokowana jest na azjatyckim brzegu rzeki Ural .
Wielki kombinat metalurgiczny spowodował , że miasto znajduje się w pierwszej 25 najbardziej zanieczyszczonych miast na świecie .
Z tego też powodu tylko co czwarte dziecko rodzi się zdrowe .
