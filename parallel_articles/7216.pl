Machmud Esambajew
Machmud Alisułtanowicz Esambajew ( ros .
Махмуд Алисултанович Эсамбаев ; ur .
15 lipca 1924 we wsi Staryje Atagi k. Groznego , zm .
7 stycznia 2000 w Moskwie ) – radziecki tancerz , aktor i choreograf pochodzenia czeczeńskiego .
Życiorys .
W 1939 rozpoczął naukę w szkole baletowej w Groznym i rozpoczął występy z Czeczeńsko-Inguskim Zespołem Pieśni i Tańca .
Wybuch wojny sprawił , że przerwał naukę w szkole i razem z brygadą koncertową występował w szpitalach , dla rannych żołnierzy .
Od 1944 , kiedy władze ZSRR przeprowadziły przymusową deportację Czeczenów , rodzina Machmuda Esambajewa znalazła się w Kirgizji .
Na okres dwunastu lat młody tancerz związał się z Kirgiskim Teatrem Opery i Baletu , działającym we Frunze .
Jako solista zespołu tańczył główne role w baletach `` Jezioro łabędzie '' , `` Fontanna Bachczysaraju '' i `` Śpiąca królewna '' .
Pod koniec lat 50 .
Esambajew zdecydował się powrócić do narodowego repertuaru czeczeńskiego .
W jego repertuarze znalazły się takie tańce jak czeczeńsko-inguski Czaban , baszkirski Woin , ale także tańce indyjskie i hiszpańskie .
Związał się z grupą `` Gwiazdy baletu radzieckiego '' ( Звёзды советского балета ) , z którą występował w Moskwie , ale także we Francji i na tournée w Ameryce Południowej .
Wystąpił w siedmiu filmach fabularnych .
Wielokrotnie wybierany deputowanym do Rady Najwyższej Czeczeńsko-Inguskiej ASRR , a także do Rady Najwyższej ZSRR .
Dzięki jego poparciu w Groznym przebudowano budynek Teatru Dramatycznego i cyrku .
W 1974 uhonorowany tytułem Ludowy Artysta ZSRR , a w 1984 Bohatera Pracy Socjalistycznej .
17 lipca 2008 pomnik tancerza odsłonięto w Groznym , w 2009 jego imieniem nazwano dawny Prospekt Rewolucji w stolicy Czeczenii .
Pochowany na Daniłowskim Cmentarzu Muzułmańskim w Moskwie .
