Moguncja
Moguncja ( ; , ) – miasto na prawach powiatu nad Renem w Niemczech , obecna stolica kraju związkowego Nadrenia-Palatynat , dawna stolica Arcybiskupstwa Moguncji w czasach Świętego Cesarstwa Rzymskiego .
W starożytności Moguncja była twierdzą dominującą na zachodnim brzegu Renu , jako część północnych kresów Cesarstwa Rzymskiego .
Miasto jest położone naprzeciw Wiesbaden , w zachodniej części aglomeracji Ren-Men , zdominowanej przez Frankfurt nad Menem .
Moguncja jest kolebką drukarstwa europejskiego i światowego na przemysłową skalę za sprawą jej znamienitego rzemieślnika Johannesa Gutenberga .
