Mairano
Mairano – miejscowość i gmina we Włoszech , w regionie Lombardia , w prowincji Brescia .
Według danych na rok 2004 gminę zamieszkiwało 2412 osób , 219,3 os./km² .
W Mairano znajduje się fabryka firmy Cacciamali , która produkuje autobusy pod swoją marką oraz dla sieci koncernu Irisbus pod jego marką .
Wykorzystuje przy tym podwozia produkowane przez koncern Iveco .
Część komponentów do produkcji pochodzi z polskiej firmy Kapena , która jest własnością Cacciamali .
