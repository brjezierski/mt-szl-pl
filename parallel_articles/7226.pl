Majowie
Majowie – grupa ludów indiańskich mówiących językami z rodziny maja , zamieszkujących południowo-wschodni Meksyk ( półwysep Jukatan i stan Chiapas ) , Gwatemalę , Belize i zach .
Honduras .
Historia .
Majowie byli twórcami wysoko rozwiniętej cywilizacji , której początki sięgają okresu preklasycznego późnego ( 400 p.n.e.–250 n.e .
) , według chronologii przyjętej dla kultur Mezoameryki .
Dziedzictwo cywilizacji Majów .
Główne osiągnięcia cywilizacji Majów w dziedzinie architektury stanowiły monumentalne zespoły przestrzenne , złożone ze świątyń na wysokich piramidach schodkowych , pałaców , tarasów , dziedzińców i kamiennych boisk do gry w piłkę oraz zastosowanie w budownictwie tzw .
