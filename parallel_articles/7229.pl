Majuro
Majuro ( ) – jeden z atoli łańcucha Ratak Chain w archipelagu Wysp Marshalla w środkowej części Oceanu Spokojnego .
W jego skład wchodzą 64 wysepki , o łącznej powierzchni 9,17 km² .
Otaczają one lagunę o powierzchni około 295,05 km² .
Majuro to także stolica Wysp Marshalla z dzielnicą rządową położoną na trzech połączonych ze sobą wysepkach Delap-Uliga-Darrit .
Według danych za rok 2011 atol zamieszkiwało łącznie 27 797 osób – najwięcej ze wszystkich atoli i wysp Wysp Marshalla .
Nazwa .
Nazwa atolu Majuro pochodzi od marsz .
`` Mājro '' oznaczającego „ dwa otwarcia ” .
