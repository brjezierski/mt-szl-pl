`` Região Administrativa Especial de Macau '' ) – jeden z dwóch specjalnych regionów administracyjnych Chińskiej Republiki Ludowej ( drugim regionem jest Hongkong ) , znajdujący się na wschodnim wybrzeżu Chin .
Makau leży po zachodniej stronie estuarium Rzeki Perłowej , graniczy od północy z prowincją Guangdong , natomiast od południa i wschodu otoczone jest wodami Morza Południowochińskiego .
Gospodarka opiera się głównie na turystyce i hazardzie , jednak prężnie rozwijają się także niektóre gałęzie przemysłu , m.in .
przemysł odzieżowy , elektroniczny , zabawkarski .
Makau zalicza się do najbogatszych regionów na świecie .
