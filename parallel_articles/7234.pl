Makiejewka
Makiejewka ( , do 1917 Dmytrijiwka , ukr .
Дмитріївка , w latach 1917–1931 Dmytryjewśkyj , Dmytrijiwśk lub Dmytrijewśkie , ukr .
Дмитріївський , Дмитріївськ , Дмитріївське ) – miasto w obwodzie donieckim na Ukrainie .
Na skutek rosyjskiej napaści na Ukrainę , od 2014 roku znajduje się pod okupacją samozwańczej Donieckiej Republiki Ludowej .
Prawa miejskie posiada od 1925 .
