Makowiec ( ciasto )
Makowiec – ciasto przekładane masą ze zmielonego maku , cukru ( lub miodu ) i bakalii .
Wypiek taki najczęściej przybiera formę :
Makowce w kuchni polskiej .
Charakterystyka .
Mak przeznaczony na masę należy odpowiednio przygotować przed zmieleniem ( lub utarciem ) .
Jednym ze sposobów jest sparzenie ( przelanie wrzątkiem ) ziaren na sitku , następnie staranne odsączenie i odparowanie .
Inna metoda to zalanie maku zimnym lub gorącym mlekiem lub wodą i odstawienie na pewien czas do namoczenia – czasem po uprzednim zagotowaniu .
Według części przepisów , po sparzeniu lub namoczeniu ziaren należy je jeszcze ugotować .
