Makówki
Makówki ( makiełki ) – deser , potrawa wigilijna .
Śląsk .
Pod nazwą „ makówki ” występuje na Śląsku .
Podstawowe składniki śląskich makówek to mak , bułki kanapkowe lub sucharki , mleko lub woda , miód lub cukier oraz bakalie ( rodzynki , figi , migdały , orzechy włoskie , wiórki kokosowe , skórka pomarańczowa , rum , pomarańcze , mandarynki ) .
Namoczone pieczywo układa się warstwami na przemian z masą makową .
Spotyka się także makówki przyrządzane na chałce .
Inne regiony .
Na Zagłębiu , okolicach Poznania i Łodzi potrawa ta znana jest jako „ makiełki ” lub „ makiołki ” .
Na Zagłębiu zamiast pieczywa używa się makaronu ( zwanego w niektórych rejonach Polski „ kluskami ” , stąd inna nazwa makiełek to „ kluski z makiem ” ) .
Historia .
W biedniejszych rodzinach makówki przygotowywano na wodzie , a zamiast bakalii dodawano suszone owoce krajowe .
