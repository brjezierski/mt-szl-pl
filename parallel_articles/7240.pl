Malabo
Malabo ( do 1973 `` Santa Isabel '' ) – stolica Gwinei Równikowej , port na wyspie Bioko w Zatoce Gwinejskiej .
Drugie co do wielkości miasto kraju ( zaraz po Bata ) .
Miasto ma utracić funkcję stolicy na rzecz będącej w budowie Oyali .
Historia .
Założone w 1827 przez Brytyjczyków na zboczu wygasłego wulkanu jako ośrodek handlu niewolnikami o nazwie `` Clarencetown '' lub `` Port Clarence '' .
Zanim Liberia stała się państwem , do którego podążali wyzwoleni niewolnicy Malabo i jego okolice było takim miejscem .
Wielu z nich pozostało na wyspie i tworzą obecnie grupę etniczną zwaną Fernandinos .
W 1843 miasto zajęli Hiszpanie i zmienili nazwę na `` Santa Isabel '' .
