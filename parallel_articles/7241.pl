Język malajalam
Język malajalam – język drawidyjski , używany w Indiach na Wybrzeżu Malabarskim i Lakszadiwach przez około 30 mln osób .
Jest językiem urzędowym w stanie Kerala i na terytorium związkowym Lakszadiwów .
Język ten jest blisko spokrewniony z językiem tamilskim , ma też własne – wywodzące się ze starotamilskiego alfabetu vatteluttu – pismo malajalam .
Jako odrębny język malajalam oddzielił się od tamilskiego ok. X w. n.e .
Pierwszy utwór literacki w tym języku , `` Ramaciaritam '' , powstał prawdopodobnie w XII wieku .
W średniowieczu istniał styl literacki nazywany manipravalam ( dosł .
„ rubiny i korale ” ) , polegający na swobodnym wykorzystaniu zasobów leksykalnych zarówno malajalam , jak i sanskrytu w jednym utworze .
W przeciwieństwie do języka tamilskiego , słownictwo malajalam nadal zawiera duży procent zapożyczeń z sanskrytu .
