Mała Panew
Mała Panew ( niem .
`` Malapane '' , hist .
też `` Maltbach '' ) – rzeka w południowo-zachodniej Polsce , prawy dopływ Odry .
Źródłowy odcinek rzeki leży w obrębie Wyżyny Śląskiej , natomiast pozostała większość biegu w obrębie Równiny Opolskiej .
Przepływa przez województwa śląskie i opolskie .
Źródła rzeki znajdują się w pobliżu miasta Koziegłowy , na polu pomiędzy wsiami Markowice , Rzeniszów i Krusin .
Stamtąd Mała Panew płynie przez Zieloną , Kalety , gminę Tworóg , Lubliniec-Kokotek , Krupski Młyn , Zawadzkie , Kolonowskie , Ozimek i Turawę .
W dolnym biegu rzeki , pomiędzy Turawą a Ozimkiem , zbudowano zbiornik retencyjny Jezioro Turawskie .
Ujście do Odry znajduje się w północnej części Opola , na obszarze dawnej wsi Czarnowąsy .
Najważniejszymi dopływami Małej Panwi są Lublinica , Stoła i Ligancja .
Mała Panew jest rzeką o meandrującym biegu i w większości piaszczystym dnie .
Jest ciekiem średnio zanieczyszczonym : źródła rzeki zaliczane są do wód III klasy czystości , a woda poniżej Jeziora Turawskiego jest nawet drugoklasowa .
Cały środkowy bieg rzeki , od Kalet do Ozimka uważa się za zanieczyszczony na tyle , by wody te zaliczyć do pozaklasowych .
Mimo wszystko jest rzeką dość ciekawą dla wędkarzy ze względu na duże pogłowie płoci , leszczy i szczupaków .
