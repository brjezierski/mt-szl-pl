Łódź .
W czerwcu 2014 roku w korycie rzeki na wysokości miejscowości Staniszcze Małe odkryto starą dłubankę o długości prawie 3,7 metrów .
Późniejsze badania metodą węgla radioaktywnego C14 wykazały , że łódź została wykonana w połowie XVIII wieku z jednego pnia dębu .
Po dokonanej konserwacji w Muzeum Archeologicznym w Biskupinie , zabytek powrócił do lokalnej izby muzealnej w Kolonowskiem .
