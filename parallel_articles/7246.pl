Malassezia dermatis
Malassezia dermatis Sugita , M .
Takash. , A. Nishikawa & amp ; Shinoda – gatunek grzybów należący do rzędu `` Malasseziales '' .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Malassezia , Malasseziaceae , Malasseziales , Incertae sedis , Malasseziomycetes , Ustilaginomycotina , Basidiomycota , Fungi '' .
Uwagi taksonomiczne : Wyizolowany został w 2002 r. na wyspie Honsiu ze zmian skórnych spowodowanych przez atopowe zapalenie skóry u ludzi .
Charakterystyka .
Na podłożu LNA po 7 dniach w temperaturze 32 °C , komórki wegetatywne są kuliste , jajowate lub elipsoidalne o wymiarach ( 2-8 ) × ( 2-10 ) μm .
Kultura jest półbłyszcząca , matowa , o gładkich krawędziach .
Teleomorfa nieznana .
`` M. dermatis '' podobny jest do `` Malassezia furfur '' .
Rozróżnić je można tylko metodami biologii molekularnej .
Najbardziej wiarygodną i najprostszą metodą identyfikacji `` M. dermatis '' jest analiza sekwencji regionów D1 i D2 26S rDNA lub regionów ITS .
`` M. dermatis '' wraz z innymi gatunkami rodzaju `` Malassazis '' wykryty został u ludzi z atopowym zapaleniem skóry w Japonii .
Dalsze badania powinny zbadać , czy ten mikroorganizm jest odpowiedzialny za atopowe zapalenie skóry lub inne choroby skóry i czy jest specyficzny dla Japonii .
