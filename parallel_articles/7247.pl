Morfologia i fizjologia .
`` Malassezia furfur '' to jednokomórkowy organizm zbudowany z komórek o butelkowatym kształcie i wielkości od 1,5–4,5 × 2,0–6,5 mikrometrów .
Ich hodowla w laboratorium jest trudna , wymagająca spełnienia określonych warunków .
W warunkach laboratoryjnych zazwyczaj tworzy się forma drożdżowa .
Najczęściej wymienianymi suplementami warunkującymi rozwój hodowli są : glicyna , cholesterol i estry cholesterolu .
Znana jest tylko anamorfa .
Rozmnażanie bezpłciowe zachodzi przez pączkowanie drożdżowej komórki macierzystej .
Chorobotwórczość .
`` Malassezia furfur '' to gatunek drożdżopodobnego grzyba naturalnie występujący na powierzchni skóry ludzi i niektórych innych ssaków .
Na ogół istnieje jako organizm komensalny stanowiący naturalną część mikroflory ludzkiej skóry , ale w pewnych warunkach może nabrać zdolności patogenicznych , przechodząc z postaci drożdżowej do strzępkowej .
Dzieje się to wskutek niezbadanych dotąd zmian molekularnych .
Może to prowadzić do jego niekontrolowanego rozmnażania się i zaburzenia równowagi flory bakteryjnej skóry .
Wywołuje różnego rodzaju schorzenia dermatologiczne , zwłaszcza łojotokowe zapalenie skóry i łupież pstry , ponadto łupież , zapalenie mieszków włosowych , zapalenie płuc związane z cewnikowaniem u pacjentów otrzymujących przeszczepy układu krwiotwórczego oraz atopowe zapalenie skóry .
