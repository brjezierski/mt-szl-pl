Malawi
Malawi , Republika Malawi ( ; czewa `` Malaŵi '' , `` Dziko la Malaŵi '' ) dawniej `` Niasa '' – państwo we wschodniej Afryce , bez dostępu do morza .
Graniczy z Tanzanią , Mozambikiem i Zambią .
Ustrój polityczny .
Malawi jest republiką wielopartyjną .
Prezydent , wybierany w głosowaniu powszechnym na pięcioletnią kadencję , jest jednocześnie szefem rządu , ustalając skład Rady Ministrów .
Głównym organem władzy ustawodawczej jest jednoizbowy parlament – Zgromadzenie Narodowe , którego 193 członków wybieranych jest w głosowaniu powszechnym na pięcioletnią kadencję .
Główne partie to : Partia Kongresu Malawi ( MCP ) , Zjednoczony Front Demokratyczny ( UDF ) , Przymierze dla Demokracji ( AFORD ) .
Geografia .
Malawi to kraj pozbawiony dostępu do morza .
Położony jest w Afryce południowo-wschodniej .
Z północy na południe kraju rozciąga się Wielki Rów Zachodni , w obrębie którego znajduje się trzecie co do wielkości jezioro afrykańskie – Niasa , powstałe w wyniku ruchów tektonicznych na obszarze Wielkich Rowów Afrykańskich .
Z południowych krańców jeziora wypływa rzeka Shire , będąca dopływem Zambezi .
Na wschód i zachód od jeziora Niasa w krajobrazie dominują wysokie płaskowyże .
Położona na północy kraju wysoczyzna Nyika sięga 2600 m n.p.m .
Na południe od jeziora Niasa rozciąga się wyżyna Shire , która przechodzi w masyw górski Mlandżi , osiągający wysokość 3002 m n.p.m .
Klimat Malawi jest podrównikowy na przeważającym obszarze kraju .
Podział administracyjny .
Malawi zostało podzielone na 3 regiony , które składają się z 28 dystryktów .
Historia .
