Maleczewo
Maleczewo ( niem .
`` Malleczewen '' ) − wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie ełckim , w gminie Ełk .
W latach 1975−1998 miejscowość administracyjnie należała do województwa suwalskiego .
Historia .
Nazwa miejscowości wywodzi się od nazwiska jej założyciela , Hieronima Maleckiego , któremu książę Albrecht Hohenzollern nadał tu majątek w 1566 roku .
W Malczewie znajdować się miała drukarnia Maleckich .
W 1783 roku wieś szlachecka liczyła tylko pięć domów , a w 1821 roku 37 mieszkańców .
W 1905 roku majątek obejmował 165 ha ; czynna tu była gorzelnia .
Podczas ostatniej wojny pracowali tu jeńcy polscy , francuscy i inni .
W czasie działań wojennych w 1945 roku zabudowania majątku zostały niemal kompletnie zniszczone .
Urzędowo miejscowość nazywała się Malleczewen .
W 1936 roku zniemczono tę nazwę na Maletten .
