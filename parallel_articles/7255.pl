Malediwy
Malediwy ( , `` Diwehi Radźdźe '' ; ofic .
Republika Malediwów , maled .
, `` Diwehi Radźdźege Dźumhurijja '' ) – archipelag oraz państwo wyspiarskie o tej samej nazwie , położone na Oceanie Indyjskim , ok. 500 km na południowy zachód od południowego krańca Indii .
W XVI wieku wyspami starali się zawładnąć Portugalczycy i Holendrzy .
Od roku 1796 ( a formalnie od 1887 ) stały się brytyjskim protektoratem .
W 1965 roku Malediwy uzyskały niepodległość , jako monarchia ( sułtanat ) .
Po trzech latach przeprowadzono referendum ( w marcu 1968 ) i w listopadzie tegoż roku proklamowano republikę .
Malediwy są krajem muzułmańskim .
Geografia .
Archipelag to ok. 1190 wysepek osadzonych na 26 atolach koralowych narosłych na podmorskim łańcuchu wulkanicznym , ciągnącym się przez 820 km z północy na południe i 120 km ze wschodu na zachód , 202 z tych wysepek jest zamieszkanych .
Łączna powierzchnia lądowa archipelagu to 298 km² , mieszkańców 396 tys .
( stan na rok 2009 ) , co daje zagęszczenie 1321 os./km² .
Na Malediwach występuje klimat równikowy wybitnie wilgotny , i dlatego jego średnia roczna suma opadów atmosferycznych wynosi 2130 mm a średnia roczna temperatura powietrza 27 °C .
Stolica Malediwów – Male , gdzie mieszka nieco ponad 1/4 ludności , jest jedynym ośrodkiem miejskim w kraju .
Najwyższy punkt leży zaledwie dwa metry ponad powierzchnią poziomu morza .
To najniższy spośród najwyższych punktów poszczególnych państw świata .
Siłą rzeczy Malediwy są zagrożone całkowitym zalaniem .
Dlatego państwo to od lat zwraca się do największych i najpotężniejszych krajów świata o pomoc w ograniczaniu efektu cieplarnianego .
Ustrój polityczny .
Państwo jest republiką .
Głową państwa i szefem rządu jest prezydent wybierany w wyborach powszechnych na 5 lat .
Władzę ustawodawczą sprawuje , składający się z 48 członków , parlament ( Madżlis ) o kadencji 5 letniej .
Podział administracyjny .
Malediwy składają się z 26 naturalnych atoli , które podzielone są na 21 jednostek administracyjnych : 20 atoli administracyjnych i miasto stołeczne Male .
Każdy z atoli administrowany jest przez naczelnika nominowanego przez prezydenta państwa .
Gospodarka .
Gospodarka Malediwów oparta jest głównie na turystyce ( 28 % produktu krajowego brutto w 2008 roku ) , i rybołówstwie ( tuńczyk ) .
Rolnictwo to uprawa głównie palmy kokosowej , drzewa chlebowego i bananów .
Przemysł jest bardzo słabo rozwinięty .
Turystyka .
W 2015 roku kraj ten odwiedziło 1,234 mln turystów ( 2,4 % więcej niż w roku poprzednim ) , generując dla niego przychody na poziomie 2,567 mld dolarów .
Problem odpadów .
Na początku lat 90. rząd w Male był zmuszony pilnie rozwiązać problem lawinowo rosnącej masy śmieci produkowanej przez rozwijający się przemysł turystyczny .
Postanowiono , że wysypisko powstanie na sztucznie usypanej , „ śmieciowej wyspie ” Thilafushi .
Pierwszy transport trafił na nią 7 stycznia 1992 .
Średnio trafiało tam 300 ton śmieci dziennie .
Większość odpadków pochodziła z ośrodków turystycznych położonych w innych częściach archipelagu .
Odpady nie są sortowane .
Dlatego na Thilafushi są też substancje niebezpieczne dla środowiska , m.in .
metale ciężkie i azbest .
Informacje turystyczne .
Pogoda : od grudnia do kwietnia – słonecznie i bezdeszczowo .
Między majem i listopadem jest nadal ciepło , lecz niebo jest często zachmurzone i występują częste opady deszczu .
Najprościej dostać się na Malediwy drogą lotniczą .
Wiele połączeń oferuje Wielka Brytania .
Transport samochodowy .
Na Malediwach obowiązuje ruch lewostronny .
Łącznie na archipelagu jest 88 km dróg , z czego na atolu : Male 60 km , Laamu 14 km i Addu 14 km .
Religia .
Na Malediwach zgodnie z konstytucją religią państwową jest islam , na podstawie tego zapisu rząd wymaga , aby wszyscy obywatele byli muzułmanami , co prowadzi do oskarżenia o instytucjonalne prześladowania nie-muzułmanów i byłych muzułmanów , którzy mieszkają w kraju .
Prezydent oraz członkowie rządu muszą być sunnitami .
Chrześcijańska działalność misyjna w tym kraju jest zabroniona .
Obcokrajowcy mogą wyznawać inną religię , pod warunkiem , że praktykują ją wyłącznie w swoich domach lub pokojach hotelowych .
