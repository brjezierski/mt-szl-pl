Malibu ( singel )
Malibu - drugi singel amerykańskiego , grungowego zespołu muzycznego Hole z płyty `` Celebrity Skin '' .
Tekst piosenki mówi o zmarłym 4 lata wcześniej legendarnym muzyku zespołu Nirvana Kurcie Cobainie , kiedy był w klinice w Malibu .
Piosenka była nominowana do nagrody Grammy Award w kategorii `` the Best Rock Performance by a Duo or Group with Vocal '' przegrała z piosenką Aerosmith `` Pink '' .
