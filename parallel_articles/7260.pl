Malijai
Malijai – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Górnej Prowansji .
Demografia .
Według danych na rok 1990 gminę zamieszkiwały 1703 osoby , a gęstość zaludnienia wynosiła 64 osoby/km² .
W styczniu 2015 r. Malijai zamieszkiwało 2018 osób , przy gęstości zaludnienia wynoszącej 75,8 osób/km² .
