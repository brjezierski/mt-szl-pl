Malinowo ( rejon szarkowszczyński )
Malinowo – dawna kolonia .
Tereny na których leżała znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie szarkowszczyńskim , w sielsowiecie Radziuki .
Nazwa dawniej używana – Malwinowo .
Historia .
W czasach zaborów w granicach Imperium Rosyjskiego .
