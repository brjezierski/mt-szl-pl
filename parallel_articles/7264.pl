Kaganiec
Kaganiec – przedmiot zakładany na pysk zwierzęcia ( najczęściej psa ) w celu uniemożliwienia mu ugryzienia człowieka lub innych zwierząt oraz połknięcia przez niego niepożądanych substancji ( np .
odchodów lub odpadków ) .
Najczęściej wykonany w formie siatki z drutu lub skóry z przerwami między prętami lub paskami dla umożliwienia zwierzęciu oddychania .
W niektórych typach kagańców otwarcie pyska uniemożliwia pas skóry lub mocnej tkaniny , krępujący pysk w sposób umożliwiający oddychanie .
Zakładanie kagańca może być wymagane prawem , w Polsce np .
obowiązuje w środkach komunikacji publicznej .
