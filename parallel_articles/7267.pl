Malnia
Malnia ( dodatkowa nazwa w j. niem .
Mallnie , dawn .
`` Malnie '' ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Gogolin .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Na terenie wsi znajduje się duże wywierzysko w wapieniach triasowych .
Położenie .
Malnia leży na 50°31 ' szerokości geograficznej północnej i na 17°58 ' długości geograficznej wschodniej .
