Język maltański
Język maltański ( `` l-Ilsien Malti '' ) – język z grupy semickiej w ramach rodziny afroazjatyckiej .
Maltański jest używany m.in .
przez około 350 tys .
osób na Malcie , gdzie od 1964 jest językiem urzędowym .
Maltański jest spokrewniony z językiem arabskim , a dokładniej z dialektami zachodnimi języka arabskiego , szczególnie z dialektem tunezyjskim .
Wiele zapożyczeń czerpał z języków włoskiego i angielskiego .
Jest to jedyny język semicki zapisywany wyłącznie alfabetem łacińskim .
Niektórzy lingwiści sądzą , że maltański może być potomkiem
wymarłego języka fenickiego .
Najstarszym znanym tekstem zapisanym po maltańsku jest `` Il Cantilena '' , wiersz pochodzący z XV wieku , pióra Pietro Caxaro .
Maltański bardzo długo pozostawał językiem wyłącznie mówionym .
W piśmiennictwie początkowo używano alfabetu arabskiego , a dopiero później łacińskiego .
Alfabet .
Alfabet maltański składa się z 5 samogłosek ( a , e , i , o , u ) i 23 spółgłosek ( b , ċ , d , f , g , ġ , h , ħ , j , k , l , m , n , p , q , r , s , t , v , w , x , ż , z ) .
Zawiera on także 1 dyftong ( ie ) i 1 dwuznak ( għ ) .
Pomimo istnienia litery ċ nie ma litery c. Ponadto , odwrotnie niż w alfabecie polskim , litera ż stoi przed literą z .
Język maltański jako język urzędowy Unii Europejskiej .
Język maltański od dnia przystąpienia Malty do Unii Europejskiej jako język urzędowy tego kraju stał się jednym z języków urzędowych i roboczych Unii .
Jednak na mocy Rozporządzenia Rady ( WE ) nr 930/2004 z dnia 1 maja 2004 w ciągu trzech lat od tego dnia ( tj .
do 30 kwietnia 2007 ) w języku maltańskim nie były sporządzane ani publikowane wszystkie akty prawne UE , lecz tylko najważniejsze .
Pod koniec tego okresu zaległe akty musiały być jednak opublikowane .
Przyczyną tego wyjątku były problemy ze znalezieniem odpowiedniej liczby tłumaczy potrzebnych do publikacji Wydania Specjalnego Dziennika Urzędowego Unii Europejskiej oraz jego wydań bieżących .
Opisany wyjątek nie naruszał charakteru języka urzędowego i roboczego języka maltańskiego w UE .
