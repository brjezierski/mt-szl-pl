Mamberamo
Mamberamo ( ) – rzeka w Indonezji ( prowincja Papua ) , na wyspie Nowa Gwinea ( w północno-zachodniej części ) , najdłuższa rzeka w zachodniej części wyspy .
Rzeka ma ok. 800 km długości ( licząc od źródeł [ Idenburg ] w Górach Śnieżnych ) , kieruje się głównie ku północnemu zachodowi .
Średni przepływ 4000 m³/s .
Główna miejscowość to Bonoi .
Powstaje z połączenia rzek Taritatu ( Idenburg ) i ( Rouffaer ; źródła w Górach Śnieżnych ) .
Uchodzi do Oceanu Spokojnego tworząc deltę nieopodal Cape Narwaku ( ang. , , ) .
Żeglowna do 150–240 km od ujścia .
Osadnictwo w postaci małych wiosek .
