Manasterz ( stacja kolejowa )
Manasterz – wąskotorowa stacja kolejowa Przeworskiej Kolei Dojazdowej na terenie Medyni Kańczuckiej , w gminie Kańczuga , w powiecie przeworskim , w województwie podkarpackim , w Polsce .
Została otwarta 8 lipca 1906 roku .
W sezonie letnim obsługuje ruch turystyczny .
