Manaus
Manaus ( wym .
[ ] ) – miasto w Brazylii , największe miasto Amazonii , przeżywające rozkwit na przełomie XIX i XX wieku oraz ponownie współcześnie .
Jest stolicą stanu Amazonas w Regionie Północnym .
Geografia .
Manaus leży nad rzeką Rio Negro , w miejscu łączenia się rzek Solimões i Rio Negro , które tworzą Amazonkę , w środku Puszczy Amazońskiej .
Jest oddalone ok. 1200 km od wybrzeża .
Gospodarka .
Jest portem bezcłowym , co pozwoliło Manaus stać się najbardziej znaczącym portem na rozległym systemie rzecznym Amazonii , dostępnym także dla statków morskich .
