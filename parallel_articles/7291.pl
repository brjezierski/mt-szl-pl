Mandurah
Mandurah – miasto w Australii , w stanie Australia Zachodnia .
Położone 72 km na południe od Perth , na wybrzeżu Oceanu Indyjskiego , pełni rolę miasta satelickiego dla Perth .
Zamieszkują je głównie młode małżeństwa i emeryci .
Obecnie liczy około 75 000 mieszkańców , lecz jest jednym z najszybciej rozrastających się młodych miast Australii .
Popularne jako cel weekendowych eskapad mieszkańców Perth .
Mandurah uzyskało status miasteczka ( `` town '' ) w 1987 roku , a miasta ( `` city '' ) w 1990 .
