Język mandżurski
Język mandżurski – język z grupy tungusko-mandżurskiej , którym posługują się nieliczni przedstawiciele Mandżurów zamieszkujących północno-wschodnie Chiny ( Mandżurię ) .
Literatura , wzorowana na chińskiej , a zapisywana pismem mandżurskim , opartym na piśmie mongolskim , powstawała już od XVII w. Język mandżurski zagrożony jest wymarciem ze względu na daleko posuniętą sinizację Mandżurów .
Na przełomie lat 80. i 90. niecałe 200 osób posługiwało się jeszcze tym językiem .
Liczba mówiących w 2007 roku wyniosła 20 osób .
