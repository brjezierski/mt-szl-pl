Mandżurowie
Mandżurowie ( mandż .
`` Manju '' ; ) – naród wywodzący się z plemion tungusko-mandżurskich ałtajskiej ligi językowej , zamieszkujących od zamierzchłych czasów północno-wschodnią Mandżurię .
Lud ten od XVII wieku do 1911 roku władał Chinami .
W tym czasie niemal zupełnie zasymilował się z Chińczykami , tracąc tym samym swą kulturową odrębność .
Obecnie stanowią mniejszość na terenie Mandżurii oraz Dżungarii , żyjąc w rozproszeniu wśród ludności chińskiej .
Historia .
Na początku XI w. z Heishui Mohe ( którzy mieli wspólne korzenie z założycielami państwa Balhae ) wyłoniła się grupa nazywająca siebie Dżurdżenami .
W 1121 opanowali większość terenów obecnej Mandżurii , rozbijając kitańskie państwo Liao i ustanawiając własne – Jin , które od połowy XII w. panowało nad północnymi Chinami .
