Light novel
– japoński gatunek powieści .
Jest ona zazwyczaj ilustrowana , skierowana głównie do młodzieży i młodszych dorosłych .
Termin `` light novel '' jest japońskim anglicyzmem ( waseieigo ) .
Stosuje się również formy skrócone i .
W ostatnich latach na podstawie takich powieści tworzone są mangi , anime , a także filmy aktorskie .
Często są one drukowane w odcinkach w magazynach literackich , takich jak Faust , Gekkean Dragon Magazine , Za Sunika czy Dengeki hp .
Z uwagi na dużą popularność japońskie wydawnictwa organizują coroczne konkursy dla autorów , w których nagrodą może być m.in .
publikacja nagrodzonej powieści .
Największym z tych konkursów jest Dengeki Shousetsu Taishou z główną nagrodą miliona jenów , na który corocznie wpływa ponad 2500 zgłoszeń .
Light novel są wyraźnie oznaczane i publikowane jako tanie edycje w miękkich okładkach .
Sfinansowane przez japoński rząd badania wskazują , że w 2006 roku rynek tego typu powieści wart był około 46 miliardów jenów , przy około 78 milionach egzemplarzy rocznej sprzedaży .
Większa część rynku ( około 70 % ) należy do Kadokawa Group Holdings .
Powieści te wydawane są również poza Japonią , przede wszystkim w Stanach Zjednoczonych .
