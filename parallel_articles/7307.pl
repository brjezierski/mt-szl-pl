Manowar
Manowar – amerykański zespół muzyczny grający heavy/power metal , założony w roku 1980 przez Joeya DeMaio i Rossa Friedmana .
Nakład wszystkich wydawnictw grupy przekroczył 30 milionów kopii .
Historia .
Początek .
Historia Manowar rozpoczyna się w roku 1980 z chwilą spotkania Joeya DeMaio oraz Rossa Friedmana w Newcastle City Hall na trasie Black Sabbath `` Heaven And Hell '' .
DeMaio pracował wtedy jako technik basowy oraz pirotechnik , a Ross grał na gitarze w zespole Shakin ' Street supportującym Black Sabbath .
Muzycy kilka miesięcy później spotykali się ponownie , tym razem w celu podjęcia współpracy .
Powstaje Manowar , niedługo potem do zespołu dołączają wokalista Eric Adams oraz perkusista Donnie Hamzik .
W 1982 roku zarejestrowany zostaje debiutancki album pt .
Battle Hymns .
Muzycy prezentują tutaj zarówno szybkie , heavymetalowe utwory , których tematem są głównie sami muzycy , wojna w imię metalu ( utwory : `` Manowar '' , `` Metal Daze '' ) , jak i podniosłe i utwory pełne patosu ( `` Battle Hymn '' ) .
