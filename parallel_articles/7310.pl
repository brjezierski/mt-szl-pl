Manteyer
Manteyer – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Wysokie .
Demografia .
Według danych na rok 1990 gminę zamieszkiwały 222 osoby , a gęstość zaludnienia wynosiła 9 osób/km² .
W styczniu 2015 r. Manteyer zamieszkiwały 433 osoby , przy gęstości zaludnienia wynoszącej 16,9 osób/km² .
