Marabá
Marabá – miasto w Brazylii , w stanie Pará , położone nad ujściem rzeki Itacaiúnas do rzeki Tocantins , ok. 450 km na południe od Belém .
W 2010 roku miasto liczyło 233 669 mieszkańców .
Przez Marabá przebiega Transamazonika , droga łącząca Amazonię z brazylijskim wybrzeżem .
W mieście znajduje się port lotniczy Marabá .
W mieście rozwinął się przemysł spożywczy , drzewny oraz chemiczny .
