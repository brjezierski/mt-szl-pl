Marasmius chordalis
Marasmius chordalis Fr .
– gatunek grzybów należący do rodziny twardzioszkowatych ( `` Marasmiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Marasmius , Marasmiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim nazwy naukowej : `` Chamaeceras chordalis '' ( Fr . )
Kuntze 1898 .
Charakterystyka .
Saprotrof wytwarzający owocniki o jasnoszarym , blaknącym do białawego , czerwonobrązowo nakrapianym kapeluszu ( średnicy przeważnie 1–2 cm ) , o oszronionej ( z powodu wystających cystyd ) skórce na powierzchni , lekko pomarszczonym , żłobkowanym brzegu i blaszkowatym hymenoforze na spodzie .
Blaszki mają brudnobiaławe zabarwienie , regularną tramę i są szeroko przyrośnięte do szklistego , brązowiejącego , oszronionego trzonu .
Gatunek ten występuje na kłączach orlicy pospolitej lub zagrzebanym drewnie .
Owocniki wytwarza we wrześniu i październiku , czasami w kępkach .
