Twardzioszek ochrowy
Twardzioszek ochrowy ( `` Marasmius collinus '' ( Scop . )
Singer ) – gatunek grzybów należący do rodziny twardzioszkowatych ( `` Marasmiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Marasmius , Marasmiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go Joannes Antonius Scopoli w 1772 r. nadając mu nazwę `` Agaricus scopolius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Rolf Singer w 1942 r .
Synonimy naukowe :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
