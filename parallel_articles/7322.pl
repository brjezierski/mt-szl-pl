Twardzioszek igłowy
Twardzioszek igłowy ( `` Marasmius wettsteinii `` Sacc .
& amp ; P .
Syd . )
– gatunek grzybów należący do rodziny twardzioszkowatych ( `` Marasmiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Marasmius , Marasmiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Zdiagnozowali go Pier Andrea Saccardo i Hans Sydow w 1899 r. Synonimy naukowe :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 2,5–7 mm , u młodych okazów półkulisty , u dojrzałych rozpostarty i często z wyraźnym wklęśnięciem na środku , biały .
Ma falisty brzeg i jest promieniście żłobiony z prześwitującymi blaszkami .
Rzadkie , bez międzyblaszek , białe , przyrośnięte w postaci kołnierzyka otaczającego trzon .
Długi , cienki , nitkowaty .
Ma wysokość 2–6 cm przy grubości 0,4–0,8 mm .
