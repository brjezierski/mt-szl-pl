Twardzioszek białawoliliowy
Twardzioszek białawoliliowy ( `` Marasmius wynneae '' Berk .
& amp ; Broome ) – gatunek grzybów należący do rodziny twardzioszkowatych ( `` Marasmiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Marasmius , Marasmiaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Czasami opisywany jako `` Marasmius wynnei '' .
Synonimów naukowych ma 15 .
Niektóre z nich :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako twardzioszek kulisty .
Morfologia .
Średnica przeważnie 1,5–5 cm .
Jest bladoszary lub szarobrązowawy ( czasami z fioletowawym odcieniem ) , higrofaniczny , w wilgotnym stanie nieco żłobkowany i prześwitujący , a w stanie suchym białawy .
Brudnobiaławe , szarobiaławe lub fioletowoszare , z regularną tramą
Czerwonobrązowy ( jaśniejszy przy kapeluszu ) , kosmkowaty .
O silnej woni , czasami lekko cyjanowodorowej .
Występowanie i siedlisko .
Opisano występowanie tego gatunku tylko w Europie .
Jest tutaj szeroko rozprzestrzeniony – od Hiszpanii po Półwysep Skandynawski .
W Polsce jego rozprzestrzenienie i częstość występowania nie są znane , nie jest jednak zagrożony wyginięciem .
W piśmiennictwie naukowym opisano wiele jego stanowiska na terenie Polski .
