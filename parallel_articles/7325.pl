Marbella
Marbella – miasto położone w południowej Hiszpanii , nad Morzem Śródziemnym .
Położona jest ok. 47 km od centrum Malagi , na obrzeżach jej aglomeracji .
Położenie i charakter .
Marbella leżąca w prowincji Malaga ( Andaluzja ) ma 143 386 mieszkańców ( 2019 ) , jest znanym kąpieliskiem morskim i ośrodkiem wypoczynku letniego .
To również port rybacki .
Ze względu na swoje walory turystyczne , jest jednym z najczęściej odwiedzanych kurortów na Costa del Sol .
Wydarzenia .
W 1982 otwarto tu pierwszy meczet wzniesiony w Hiszpanii od czasu rekonkwisty i zakończenia obecności arabskiej w XV wieku .
