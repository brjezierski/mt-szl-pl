Młodość i wykształcenie .
Urodziła się w Katowicach w górniczej i związkowej rodzinie .
W dzieciństwie zachorowała na nowotwór kości ; choroba ta według niej miała znaczący wpływ na późniejsze podjęcie działalności politycznej .
Ukończyła studia licencjackie w Instytucie Polityki Społecznej Uniwersytetu Warszawskiego .
Zamieszkała w Warszawie .
Działalność polityczna .
Należała do stowarzyszenia Młodzi Socjaliści oraz do Partii Zieloni , w której od 2014 była członkinią zarządu partii oraz przewodniczącą jej warszawskiego koła .
W wyborach w 2014 kandydowała z listy Zielonych do Parlamentu Europejskiego .
W tym samym roku była kandydatką do rady miasta w Warszawie , kierowała też sztabem Joanny Erbel , ubiegającej się o funkcję prezydenta Warszawy w wyborach samorządowych .
W 2015 wystąpiła z Zielonych , należała w tym samym roku do założycieli Partii Razem .
W maju 2015 podczas kongresu założycielskiego wybrana do Zarządu Krajowego partii , a następnie wybierana na kolejne kadencje zarządu .
W 2015 kandydowała z listy tego ugrupowania do Sejmu z pierwszego miejsca na liście w okręgu katowickim .
Otrzymała 8316 głosów , nie uzyskując mandatu ; Partia Razem nie przekroczyła progu wyborczego .
W 2018 została umieszczona na opublikowanej przez magazyn „ Forbes ” europejskiej liście `` Forbes 30 Under 30 '' , zawierającej wpływowe osoby poniżej trzydziestego roku życia , za udział w założeniu Razem oraz w organizacji tzw .
