Marcelina Zawisza
Marcelina Monika Zawisza ( ur .
3 maja 1989 w Katowicach ) – polska działaczka społeczna i polityczna , członkini Zarządu Krajowego Lewicy Razem ( 2015–2020 ) , posłanka na Sejm IX kadencji , pierwsza wiceprzewodnicząca Koalicyjnego Klubu Parlamentarnego Lewicy .
W 2018 umieszczona na liście `` Forbes 30 Under 30 '' .
