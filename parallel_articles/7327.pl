W wyborach w 2019 bezskutecznie ubiegała się o mandat posłanki do Parlamentu Europejskiego z listy komitetu Lewica Razem , który nie przekroczył progu wyborczego .
W wyborach krajowych w tym samym roku jako liderka listy SLD ( w ramach porozumienia między ugrupowaniami lewicowymi ) w okręgu opolskim została wybrana do Sejmu IX kadencji .
W parlamencie została członkinią Komisji Zdrowia oraz pierwszą wiceprzewodniczącą Koalicyjnego Klubu Parlamentarnego Lewicy .
