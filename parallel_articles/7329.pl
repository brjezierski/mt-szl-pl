Marcin Banot
Marcin Banot ( ur .
1988 w Świętochłowicach ) – polski wspinacz builderingowy , wideobloger , przedsiębiorca
Od urodzenia mieszka w Chropaczowie , dzielnicy Świętochłowic .
26 listopada 2016 rozpoczął nagrywanie videobloga „ BNT ” , który publikowany jest na YouTube .
Osiągnięcia wspinaczkowe .
Wspiął się m.in .
na szczyt Diabelskiego Młyna w Śląskim Wesołym Miasteczku , nieczynny maszt radiowy o wysokości 363 m oraz Hotel Silesia bez asekuracji .
2 czerwca 2019 wspiął się bez asekuracji na hotel Marriott .
Wspinaczkę rozpoczął o godzinie 20:28 , po 37 minutach i 45 sekundach udało mu się bezpiecznie wdrapać na szczyt budynku .
18 września 2020 roku wspiął się na szczyt paryskiego wieżowca Montparnasse bez zabezpieczeń .
Zajęło mu to 1 godzinę i 12 minut .
Życie prywatne .
Banot na co dzień posługuje się etnolektem śląskim ( śl .
ślůnskům godkům ) .
Poza kanałem BNT prowadzi również kanał pt .
„ Marcin Banot ” , który poświęcony jest głównie vlogom lub materiałom okołowspinaczkowym .
