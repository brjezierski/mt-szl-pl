Marcin Malinowski ( piłkarz )
Marcin Malinowski ( ur .
6 listopada 1975 roku w Wodzisławiu Śląskim ) – polski piłkarz grający na pozycji defensywnego pomocnika .
Od 22 grudnia 2022 asystent trenera w Zagłębiu Sosnowiec .
Ma na swoim koncie 458 występów w polskiej Ekstraklasie ( 303 w Odrze Wodzisław Śląski ) , co czyni go drugim zawodnikiem z największą liczbą występów w historii Ekstraklasy .
Kariera piłkarska .
Początki kariery .
Karierę zaczynał w ROW Rybnik i Gwarku Zabrze , następnie trafił do Poloni Bytom skąd przeszedł do występującej w Ekstraklasie Odry Wodzisław Śląski w której spędził większość kariery .
Odra Wodzisław .
Wiosną 1997 roku trafił do wodzisławskiej Odry .
W Ekstraklasie zadebiutował 8 marca 1997 w meczu Odry przeciwko Górnikowi Zabrze .
W rundzie wiosennej sezonu 1996/1997 wystąpił 14 razy i strzelił jedną bramkę .
Odra Wodzisław zajęła 3. miejsce w Ekstraklasie co uprawniało ją do udziału w rozgrywkach Pucharu UEFA i Pucharu Intertoto .
W Odrze grał do końca sezonu 2001/2002 po czym na dwa sezony ( 2002/2003 i 2003/2004 ) trafił do Ruchu Chorzów zaliczając tam tylko 25 występów .
W sezonie 2004/2005 powrócił do drużyny z Wodzisławia Śląskiego i występował w niej do czasu aż po 14 latach spadła ona z Ekstraklasy .
Uważany jest za jednego z najlepszych piłkarzy w historii Odry .
W Odrze Wodzisław Śląski w ciągu 12 sezonów strzelił 10 bramek oraz rozegrał 303 spotkania w Ekstraklasie , dzięki czemu trafił do elitarnej grupy – Klubu 300 .
Ruch Chorzów .
Pierwszy raz Malinowski trafił do Ruchu w roku 2002 , rozgrywając dla „ Niebieskich ” dwa sezony : 2002/03 i 2003/04 po czym powrócił do wodzisławskiej Odry .
Do Chorzowa ponownie trafił po spadku Odry z Ekstraklasy latem 2010 ( sezony od 2010/11 do 2014/15 ) .
W sezonie 2011/12 z „ Niebieskimi ” zdobył wicemistrzostwo Polski i grał w finale Pucharu Polski .
W czerwcu 2015 roku Ruch Chorzów nie przedłużył kontraktu z 39-letnim kapitanem Marcinem Malinowskim , który w sumie bronił barw `` Niebieskich '' przez siedem lat .
W barwach Ruchu rozegrał łącznie 7 sezonów , w tym 6 w Ekstraklasie .
Dalsza kariera .
2 lipca 2015 zawodnik podpisał kontrakt z III ligowym Porońcem Poronin , a od 2016 do końca 2018 r. występował w IV ligowym klubie MKP Centrum Wodzisław Śląski .
Kariera trenerska .
W styczniu 2019 został grającym trenerem IV ligowej Unii Turza Śląska .
Od 30 września 2019 do kwietnia 2020 był asystentem trenera Dariusza Dudka w Zagłębiu Sosnowiec .
Następnie pełnił taką samą funkcję , również z Dudkiem , w Sandecji Nowy Sącz ( od listopada 2020 ) i ponownie w Zagłębiu Sosnowiec ( od 22 grudnia 2022 ) .
