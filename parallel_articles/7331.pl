Marcjanów ( powiat turecki )
Marcjanów – wieś w Polsce położona w województwie wielkopolskim , w powiecie tureckim , w gminie Kawęczyn .
W latach 1975–1998 miejscowość należała administracyjnie do województwa konińskiego .
Obecnie wieś sukcesywnie wyludnia się .
Liczy zaledwie 29 gospodarstw .
We wsi znajduje się : młyn gospodarczy , piekarnia , 2 sklepy spożywczo-przemysłowe , zakład tartaczny , sala taneczna wynajmowana na wesela i inne zabawy .
Podczas II wojny światowej część wsi zajmowało getto żydowskie `` Czachulec '' .
We wsi znajduje się zabytkowa kapliczka maryjna , powstała w połowie XIX wieku ( rok 1864 lub 1884 ) .
W kwietniu 2009 roku rozebrana w celu przebudowy – dzięki interwencji Wojewódzkiego Konserwatora Zabytków została przywrócona do pierwotnego , historycznego stanu .
