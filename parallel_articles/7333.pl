Marcos Paz
Marcos Paz – miasto w Argentynie , położone w północnej części prowincji Buenos Aires .
Opis .
Miejscowość została założona 25 października 1878 roku .
Przez miasto przebiega droga krajowa RP40 i linia kolejowa .
W odległości 58 km od miasta , znajduje się aglomeracja i stolica kraju Buenos Aires .
