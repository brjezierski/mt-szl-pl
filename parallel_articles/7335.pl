Marcus Stephen
Marcus Ajemada Stephen ( ur .
1 października 1969 w Anetan na Nauru ) – nauruański polityk i sportowiec , deputowany , prezydent Nauru od 19 grudnia 2007 do 10 listopada 2011 .
Były zawodowy sztangista , prezes Nauruańskiego Komitetu Olimpijskiego od 2009 .
Młodość i życie prywatne .
Urodził się 1 października 1969 w dystrykcie Anetan na Nauru .
Jest synem Lawrence ’ a Stephena .
Uczęszczał do miejscowej szkoły podstawowej Aiwo Primary School .
Następnie kształcił się w St. Bedes College w Mentone w Australii oraz w Royal Melbourne Institute of Technology w Melbourne , na którym uzyskał dyplom z księgowości .
