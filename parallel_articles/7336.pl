Marczyce
Marczyce ( do 1945 niem .
`` Marzedorf '' , w 1946 także `` Marcinkowice '' ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie karkonoskim , w gminie Podgórzyn .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa jeleniogórskiego .
