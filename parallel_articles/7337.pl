Marek Belka
( ur .
9 stycznia 1952 w Łodzi ) – polski ekonomista i polityk , profesor nauk ekonomicznych , nauczyciel akademicki i urzędnik w międzynarodowych instytucjach finansowych .
Prezes Rady Ministrów w latach 2004–2005 .
Wiceprezes Rady Ministrów i minister finansów w 1997 w rządzie Włodzimierza Cimoszewicza oraz w latach 2001–2002 w rządzie Leszka Millera .
W latach 2004–2005 przewodniczący Komitetu Integracji Europejskiej , w 2005 minister sportu w swoim drugim rządzie , w latach 2010–2016 prezes Narodowego Banku Polskiego , poseł do Parlamentu Europejskiego IX kadencji ( od 2019 ) .
Życiorys .
Wykształcenie i działalność naukowa .
Syn Władysława i Heleny ; pochodzi z rodziny inżynierów .
Edukację szkolną rozpoczął bezpośrednio od drugiej klasy szkoły podstawowej .
Ukończył IV Liceum Ogólnokształcące im .
Emilii Sczanieckiej w Łodzi , gdzie zdał maturę w wieku 16 lat .
W 1972 ukończył studia ekonomiczne na Wydziale Ekonomiczno-Socjologicznym Uniwersytetu Łódzkiego .
W 1973 podjął pracę w Katedrze Ekonomii na tej uczelni początkowo na stanowisku asystenta .
W 1978 uzyskał stopień naukowy doktora , natomiast w 1986 habilitował się .
