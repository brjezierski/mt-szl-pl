Marek Grechuta
Marek Michał Grechuta ( ur .
10 grudnia 1945 w Zamościu , zm .
9 października 2006 w Krakowie ) – polski piosenkarz , poeta , kompozytor i malarz , z wykształcenia architekt .
Uznawany za najważniejszego przedstawiciela polskiej poezji śpiewanej , wykorzystywał często elementy rocka , jazz-rocka i rocka progresywnego , szczególnie w początkowym okresie twórczości .
Zadebiutował w 1967 .
Zdobywał nagrody na Studenckim Festiwalu Piosenki w Krakowie i Krajowym Festiwalu Piosenki Polskiej w Opolu .
Pierwsze dwie płyty nagrał z zespołem Anawa , w tym krążek pt .
`` Marek Grechuta & amp ; Anawa '' ( 1970 ) , z którego pochodzą przeboje „ Będziesz moją panią ” i „ Nie dokazuj ” .
W 1971 założył Grupę WIEM .
W latach 1976–1986 związany był z Piwnicą pod Baranami .
W 2001 otrzymał Nagrodę Polskiego Przemysłu Fonograficznego Złoty Fryderyk za całokształt twórczości artystycznej .
Młodość .
Urodził się w Zamościu w 1945 .
W dzieciństwie wyjechał z rodziną do Chylic pod Warszawą , ale w 1956 rodzina na powrót osiadła w Zamościu .
W czasie pobytu w Chylicach kościelny organista udzielał Grechucie pierwszych lekcji muzyki .
Od najmłodszych lat uczył się gry na fortepianie .
Ukończył zamojskie I Liceum Ogólnokształcące im .
Jana Zamoyskiego .
W 1963 podjął studia architektoniczne na Politechnice Krakowskiej .
Kariera muzyczna .
