Marek Plura
Marek Mirosław Plura ( ur .
18 lipca 1970 w Raciborzu , zm .
20 stycznia 2023 w Katowicach ) – polski polityk , psychoterapeuta i działacz społeczny , poseł na Sejm VI i VII kadencji , poseł do Parlamentu Europejskiego VIII kadencji , senator X kadencji .
Życiorys .
Do 1991 mieszkał w Raciborzu , a następnie w Ośrodku dla Osób Niepełnosprawnych „ Miłosierdzie Boże ” w Borowej Wsi .
Ukończył studia pedagogiczne w Wyższej Szkole Filozoficzno-Pedagogicznej „ Ignatianum ” w Krakowie , a także studia podyplomowe z zakresu psychoterapii na Uniwersytecie Śląskim w Katowicach .
Pracował w Ośrodku Pomocy Kryzysowej Stowarzyszenia na rzecz Niepełnosprawnych SPES jako pracownik socjalny oraz w Ośrodku Rewalidacyjnym „ Akcent ” jako pedagog .
Był założycielem Stowarzyszenia „ Dobry Dom ” i prezesem Śląskiego Stowarzyszenia Edukacji i Rehabilitacji Osób Niepełnosprawnych „ Akcent ” , a także przewodniczącym rady programowej Działu Integracyjno-Biblioterapeutycznego Biblioteki Śląskiej .
Pełnił funkcję wicedyrektora Very Special Arts Poland .
W 2002 zainicjował Konkurs Lady D. promujący niepełnosprawne kobiety aktywne w różnych dziedzinach życia .
Działał w Europejskim Parlamencie Osób Niepełnosprawnych .
W wyborach parlamentarnych w 2005 bezskutecznie kandydował do Sejmu , uzyskując 2707 głosów .
Rok później został wybrany do Rady Miasta Katowice .
W wyborach parlamentarnych 2007 , startując z listy Platformy Obywatelskiej w okręgu katowickim , został wybrany na posła z wynikiem 10 528 głosów .
W 2011 z powodzeniem ubiegał się o reelekcję otrzymując 22 795 głosów .
W Sejmie był członkiem Komisji Polityki Społecznej i Rodziny oraz Komisji Mniejszości Narodowych i Etnicznych .
