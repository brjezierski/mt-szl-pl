Marek Szołtysek
Marek Szołtysek ( ur .
1963 w Gierałtowicach ) – śląski pisarz , dziennikarz , historyk , autor książek związanych z kulturą i kuchnią śląską , redaktor Gazety Rybnickiej .
Życiorys .
Jest absolwentem Katolickiego Uniwersytetu Lubelskiego .
Od wielu lat mieszka w Rybniku , gdzie pracuje jako nauczyciel historii i wiedzy o społeczeństwie w IV Liceum Ogólnokształcącym .
W TVS prowadzi program S2 Śląsk .
W Radiu Fest można usłyszeć jego felietony w audycji `` Szołtysek na Fest '' .
Autor cotygodniowych felietonów publikowanych w Dzienniku Zachodnim i katowickim wydaniu Echa Miasta .
