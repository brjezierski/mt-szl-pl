Maria Kaczyńska ( 1907–1942 )
Maria Kaczyńska ( ur .
8 czerwca 1907 w Przasnyszu , zm .
17 grudnia 1942 w Przasnyszu ) – pocztowiec , nauczycielka tajnego nauczania , członek ZWZ-AK .
Życiorys .
Pochodziła z rodziny rolniczej .
Była córką Ignacego i Konstancji Kaczyńskich .
Absolwentka Gimnazjum Koedukacyjnego w Przasnyszu ( matura 1928 ) .
Studiowała polonistykę na Uniwersytecie Warszawskim .
Była pracownikiem poczt w powiecie przasnyskim i Piotrkowie Trybunalskim , udzielała korepetycji .
W czasie okupacji hitlerowskiej zajmowała się tajnym nauczaniem młodzieży .
Kierowała komórką Biura Informacji i Propagandy w Obwodzie AK .
Aresztowana 22 sierpnia 1942 .
Więziona była w Płocku .
13 listopada 1942 przewieziona została do niemieckiego obozu koncentracyjnego Soldau ( KL ) w Działdowie , tam sądzona i skazana 4 grudnia 1942 na karę śmierci .
Powieszona w publicznej egzekucji na rynku przasnyskim 17 grudnia 1942 .
Razem z nią zginęli : Marian Arct „ Luty ” , Zenon Kiembrowski „ Konrad ” , Władysław Otłowski „ Woyna ” i Edmund Zdanowski „ Max ” .
Spoczywa w zbiorowym grobie na cmentarzu parafialnym w Przasnyszu .
9 maja 1972 w miejscu straceń na przasnyskim rynku odsłonięto granitowy pomnik z tablicą upamiętniającą 5 powieszonych członków sztabu ZWZ-AK .
Pozostawiła pamiętnik spisany w latach 1926-1933 ( rękopis w zbiorach rodziny autorki ) .
