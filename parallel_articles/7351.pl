Norma Jeane Mortenson urodziła się 1 czerwca 1926 o godz .
9:30 w County Hospital w Los Angeles .
Była córką Gladys Pearl Baker z d. Monroe i Charlesa Stanleya Gifforda , montażystów w Hollywoodzkiej Wytwórni Filmowej Consolidated Film Industries .
W jej akcie urodzenia ( oraz w akcie ślubu z Arthurem Millerem ) jako jej ojciec widnieje Martin Edward Mortenson , drugi mąż Gladys , który opuścił rodzinę tuż przed narodzinami Normy i z którym Gladys rozwiodła się w 1928 .
Miała dwoje rodzeństwa przyrodniego : Roberta ( 1917–1933 ) i Berniece ( 1919-2014 ) , dzieci Gladys z jej pierwszego małżeństwa z Johnem Newtonem Bakerem , które po rozwodzie rodziców w 1923 zamieszkały z ojcem .
W związku z tym , że Gladys nie mogła zrezygnować z pracy na rzecz opieki nad córką , 13 czerwca 1926 Norma trafiła pod opiekę rodziny zastępczej – Alberta i Idy Bolenderów , gorliwych protestantów .
Matka łożyła na utrzymanie córki i sporadycznie ją odwiedziała , aż w 1933 odebrała ją i wspólnie zamieszkały przy ulicy Arbol Drive 6812 w Hollywood wraz z rodziną aktorską Atkinsonów .
15 stycznia 1934 Gladys zachorowała na schizofrenię paranoidalną ( stwierdzono , że jest niepoczytalna ) i została pacjentką Metropolitan State Hospital w Los Angeles .
Resztę życia spędziła w izolacji i rzadko miała okazję spotkać się z córką .
Norma wówczas trafiła pod opiekę Grace McKee , przyjaciółki Gladys , po czym trafiła do sierocińca , w którym pozostała do czerwca 1937 .
Po latach tak wspominała ten czas : `` Rodzice wszystkich dzieci w sierocińcu umarli .
Ja miałam co najmniej jednego rodzica – matkę , ale ona mnie nie chciała .
Zbyt się wstydziłam , aby próbować wyjaśnić to innym dzieciom .
Szczęśliwa byłam tylko wtedy , gdy zabierano nas do kina '' .
Wychowywało ją łącznie 11 różnych rodzin zastępczych , m.in .
Olivia Monroe ( szwagierka Gladys Monroe ) i Edith Ana Atchinson Lower ( ciotka Grace McKee ) .
Twierdziła , że gdy miała osiem lat , padła ofiarą molestowania seksualnego ze strony mężczyzny o nazwisku Kimmel , lokatora rodziny zastępczej , u której w tym czasie mieszkała , ponadto zgwałcić mieli próbować ją Ervin Goddard ( mąż Grace McKee ) oraz jej starszy kuzyn .
Jako dziecko jąkała się i była dość wstydliwa .
