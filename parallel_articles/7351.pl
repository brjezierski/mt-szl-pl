Marilyn Monroe
Marilyn Monroe , właśc .
Norma Jeane Mortenson ( wym .
; ur .
1 czerwca 1926 w Los Angeles , zm .
5 sierpnia 1962 tamże ) – amerykańska aktorka , modelka , piosenkarka i producentka filmowa .
Gwiazda filmowa , ikona kultury popularnej oraz seksbomba lat 50. i 60 .
XX w. American Film Institute umieścił ją na szóstym miejscu na liście największych aktorek wszech czasów ( `` The 50 Greatest American Screen Legends '' ) .
Karierę zaczęła w latach 40. jako pin-up girl .
Później trafiła do świata filmu , gdzie pracowała dla 20th Century Fox ( 1946–47 ) i Columbia Pictures ( 1948 ) .
W 1951 związała się z wytwórnią Fox , wkrótce później zyskała popularność dzięki występom w filmach : `` Małpia kuracja '' , `` Niagara '' , `` Mężczyźni wolą blondynki '' czy `` Słomiany wdowiec '' .
Zasłynęła z ról stereotypowych blondynek , ale wcielała się również w postacie bardziej złożone i dramatyczne .
W 1955 studiowała aktorstwo metodyczne w Actors Studio u Lee Strasberga i założyła własną wytwórnię – Marilyn Monroe Productions .
Za rolę Elsie w `` Księciu i aktoreczce '' otrzymała nagrody David di Donatello i Crystal Star Award , a za rolę Sugar „ Kane ” Kowalczyk w `` Pół żartem , pół serio '' zdobyła Złoty Glob .
Film `` Skłóceni z życiem '' był ostatnim w jej karierze .
Wiele uwagi prasa poświęcała życiu prywatnemu Monroe .
Aktorka zmagała się z uzależnieniami , m.in .
od alkoholu i barbituranów , a także z zaburzeniami psychicznymi .
Była trzykrotnie rozwiedziona , a jej małżeństwa z baseballistą Joe DiMaggio i dramaturgiem Arthurem Millerem były przedmiotem zainteresowania mediów .
Nie miała dzieci , choć spekuluje się , iż w okresie nastoletnim urodziła syna , który został jej odebrany krótko po porodzie .
Zmarła w 1962 w wieku 36 lat z powodu przedawkowania barbituranów w swoim domu w Los Angeles .
Choć jej śmierć została uznana za „ prawdopodobne samobójstwo ” , wokół niej pojawiło się kilka teorii spiskowych .
