Mariupol ( stacja kolejowa )
Mariupol ( ukr .
: `` Станція Маріуполь '' , ros : `` Станция Мариу́поль '' ) – stacja kolejowa w Mariupolu , w obwodzie donieckim , na Ukrainie .
Położony w południowej części miasta w rejonie primorskim na Słobódka .
Znajduje się na dwutorowej zelektryfikowanej linii kolejowej z północy na południowy zachód .
Na południowy zachód znajduje się stacja towarowa Mariupol Port , na północ - stacja pasażerska Sartana .
Stacja posiada budynek stacji , lokomotywownię i wagonownię .
Węzeł kolejowy Mariupol służy jako łącznik z portem morskim w głąb lądu , zapewnia dostawy towarów masowych do miasta , eksport produktów dla przedsiębiorstw .
Na krajowej sieci głównych linii znajduje się na linii Mariupol-Jasinowataja .
