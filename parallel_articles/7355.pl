Mariusz Stępiński
Mariusz Stępiński ( ur .
12 maja 1995 w Sieradzu ) – polski piłkarz występujący na pozycji napastnika we włoskim klubie Hellas Werona , reprezentant Polski , brązowy medalista Mistrzostw Europy U-17 2012 , uczestnik Mistrzostw Europy 2016 .
Kariera klubowa .
Mariusz Stępiński jest wychowankiem Piasta Błaszki .
Podczas kariery juniorskiej występował również w Pogoni-Ekolog Zduńskiej Woli , a następnie występował w pierwszej drużynie tego klubu , w IV lidze , gdzie został zauważony przez ówczesnego trenera Widzewa Łódź , Radosława Mroczkowskiego .
W łódzkim klubie zadebiutował w wieku 16 lat , zmieniając w 90 minucie wygranego meczu z Lechem Poznań , Piotra Grzelczaka .
Mariusz stał się wówczas najmłodszym zawodnikiem jaki kiedykolwiek zagrał w meczu Widzewa w Ekstraklasie .
Młody napastnik stał się też najmłodszym debiutantem spośród wszystkich ligowców w sezonie 2011/2012 .
