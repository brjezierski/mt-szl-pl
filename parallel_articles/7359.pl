Mark Knopfler
Mark Freuder Knopfler ( ur .
12 sierpnia 1949 w Glasgow ) – brytyjski gitarzysta , kompozytor i wokalista .
Przed rozpoczęciem kariery muzycznej pracował także jako dziennikarz i wykładowca .
Jest najbardziej znany jako lider zespołu Dire Straits , ale grał także z innymi zespołami ( przede wszystkim z Notting Hillbillies ) i artystami ( między innymi z Chetem Atkinsem , Erikiem Claptonem , Tiną Turner , Bobem Dylanem , Philem Lynottem , B.B Kingiem , Philem Collinsem , Stingiem ) .
Jest także kompozytorem muzyki filmowej .
Magazyn `` Rolling Stone '' umieścił go w 2003 roku na 27. miejscu listy najlepszych gitarzystów wszech czasów .
Na liście wydanej w 2011 roku jest na 44. pozycji .
Życiorys .
Urodził się w Szkocji , w Glasgow jako syn żydowskiego architekta ( przybyłego z Węgier do Wielkiej Brytanii w obawie przed prześladowaniami za antyfaszystowskie poglądy ) i Angielki .
W połowie lat pięćdziesiątych przeprowadził się wraz z rodziną do Newcastle upon Tyne .
Na gitarze nauczył się grać jeszcze jako nastolatek , ale został profesjonalnym muzykiem dopiero w latach siedemdziesiątych , po ukończeniu uniwersytetu i kilkuletniej pracy dziennikarza .
