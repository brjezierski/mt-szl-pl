Market Harborough
Market Harborough – miasto w Wielkiej Brytanii , w Anglii w hrabstwie Leicestershire , w dystrykcie Harborough , położone na południu hrabstwa , nad rzeką Welland , 23 km na południe od Leicester i 16 km na północ od Kettering .
Stacja kolejowa na trasie Londyn - Leeds .
Miasto zamieszkuje 20 785 osób .
Historia .
Miasto założono w drugiej połowie dwunastego wieku , głównie dla celów handlowych i taką funkcję pełniło aż do dwudziestego wieku .
W średniowieczu podzielono centrum na czterdzieści osiem parcel ( yards ) – każda należała do innego kupca .
Ślady tego podziału zachowały się aż do dziś .
