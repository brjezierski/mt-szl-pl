Historia .
Miejscowość wzmiankowana w dokumencie z 1290 r. Od początku XIV w. należała do uposażenia świątobliwej Eufemii .
W latach 1945-1954 siedziba gminy Markowice .
W 1936 r. zmieniono niemiecką nazwę na `` Markdorf '' .
Wieś włączono do Raciborza w 1977 r .
