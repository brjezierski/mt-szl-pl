Markowice ( Racibórz )
Markowice ( niem .
`` Markowitz '' , 1936-1945 `` Markdorf '' ) – dzielnica Raciborza oddalona od centrum miasta o ok. 5 km w kierunku północno-wschodnim , na trasie Racibórz-Gliwice , dawniej wieś , włączona do Raciborza 1 lutego 1977 r .
W latach 1954-1972 jako wieś należały i były siedzibą gromady Markowice .
