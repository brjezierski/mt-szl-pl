Marlene Dietrich
Marlene Dietrich , właśc .
Marie Magdalene Dietrich ( ur .
27 grudnia 1901 w Schönebergu , zm .
6 maja 1992 w Paryżu ) – niemiecko-amerykańska aktorka i piosenkarka .
Pierwsze role zagrała już w latach 20. , jednak dopiero film `` Błękitny anioł '' z 1930 roku przyniósł jej międzynarodowy rozgłos .
Największe sukcesy filmowe osiągała w latach 1930–1935 , kiedy współpracowała z austriackim reżyserem Josephem von Sternbergiem .
Podczas II wojny światowej opowiedziała się przeciw III Rzeszy , występując na frontach dla amerykańskich żołnierzy .
W kolejnych dekadach skupiła się na karierze estradowej .
Ma na swoim koncie ponad 30 głównych kreacji filmowych .
Jej najsłynniejsze filmy to `` Błękitny anioł '' , `` Maroko '' , `` Szanghaj Ekspres '' , `` Destry znowu w siodle '' , `` Świadek oskarżenia '' i `` Wyrok w Norymberdze '' .
Dokonała ogromnej liczby nagrań płytowych .
Przyczyniła się do spopularyzowania wojennego szlagieru „ Lili Marleen ” .
Jej inne przeboje to m.in .
„ Falling in Love Again ” , „ Ich bin die fesche Lola ” , „ Johnny ” , „ The Boys in the Backroom ” i „ Where Have All the Flowers Gone ” .
Pozostaje jedną z najsłynniejszych aktorek XX wieku oraz ikoną kina .
W 1999 roku trafiła na 9. miejsce listy najlepszych aktorek wszech czasów , skompilowanej przez Amerykański Instytut Filmowy .
Życiorys .
Urodziła się w Berlinie , w zamożnej rodzinie mieszczańskiej , jej ojciec był berlińskim oficerem policji .
Po ponownym zamążpójściu matki nie została oficjalnie adoptowana przez ojczyma i jej nazwisko nigdy nie brzmiało von Losch .
Początkowo uczyła się gry na skrzypcach , w 1921 wstąpiła do szkoły teatralnej .
