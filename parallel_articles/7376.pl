Marlow ( Buckinghamshire )
Marlow – miasto w Wielkiej Brytanii , w Anglii , w hrabstwie Buckinghamshire , położone nad Tamizą .
W 2001 liczba ludności miasta wynosiła 17 522 .
Pierwsza wzmianka o Marlow pochodzi z początku XI w. W drugiej połowie tego stulecia król Anglii Wilhelm Zdobywca darował miasto swej żonie .
Później pozostawała m.in .
w rękach lordów Gloucester i Warwick .
Stanowiło ważny punkt na szlaku handlowym biegnącym Tamizą do Londynu .
W mieście znajduje się kilka zabytków , m.in .
most na Tamizie pochodzący z XIX w. i stanowiący kopię Mostu Łańcuchowego w Budapeszcie .
