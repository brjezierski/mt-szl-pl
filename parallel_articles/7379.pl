Marnes-la-Coquette
Marnes-la-Coquette – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Według danych na rok 2012 gminę zamieszkiwały 1 634 osoby , a gęstość zaludnienia wynosiła 470 osób/km² .
Wśród 1287 gmin regionu Île-de-France Marnes-la-Coquette plasuje się na 787. miejscu pod względem powierzchni .
