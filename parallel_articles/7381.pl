Maroko
Maroko , Królestwo Marokańskie ( , trb .
`` Al-Mamlaka al-Maghribijja '' ; tamazight , trb .
`` Tagldit N Lmaġrib '' ) – państwo położone w północno-zachodniej Afryce ze stolicą w Rabacie , należące do państw Maghrebu .
Maroko od północy otacza Morze Śródziemne , a od zachodu Ocean Atlantycki .
Na wschodzie graniczy z Algierią , a na południu ze spornym terytorium Sahary Zachodniej .
Maroko rości sobie również prawo do hiszpańskich eksklaw Ceuty , Melilli i Peñón de Vélez de la Gomera oraz kilku małych wysp u wybrzeży kontrolowanych przez Hiszpanię .
Zajmuje powierzchnię 446 300 km² lub 710 850 km² z populacją około 37 milionów .
Oficjalną i dominującą religią jest islam , a językami urzędowymi są arabski i berberyjski ; powszechnie używa się również marokańskiego dialektu arabskiego i francuskiego .
Marokańska tożsamość i kultura jest żywą mieszanką kultur berberyjskich , arabskich i europejskich .
Stolicą kraju jest Rabat , a największym miastem Casablanca .
Geografia .
Granice Maroka .
Od wschodu graniczy z Algierią , a od północy z Hiszpanią ( Hiszpańskie posiadłości w Afryce Północnej w tym Ceutą i Melillą ) .
Formalnie na południu graniczy z Saharą Zachodnią , choć Maroko próbuje administrować tym regionem .
