Marquess
Marquess – niemiecki zespół muzyczny , tworzący głównie popowe utwory hiszpańskojęzyczne , założony w 2006 .
Historia zespołu .
Zespół został założony w 2006 w Hanowerze .
Wokalistą zespołu od początku istnienia jest pochodzący z Włoch piosenkarz i producent muzyczny Sascha Pierro , który w 2003 z piosenką „ Wenn Grenzen fallen ” startował w niemieckich eliminacjach do 48 .
Konkursu Piosenki Eurowizji .
Jako grupa Marquess zadebiutował latem 2006 singlem „ El temperamento ” , który promował debiutancki album zespołu , zatytułowany po prostu `` Marquess '' .
25 maja 2007 muzycy wydali swój drugi singiel „ Vayamos Compañeros ” , który podbił listy przebojów w wielu krajach Europy .
Latem 2007 wydali drugi album studyjny , zatytułowany `` Frenetica '' .
27 czerwca 2008 wydali album , zatytułowany `` ¡YA !
`` , promowany przez single : „ La histeria ” , „ Marquess-La vida es limonada ” i „ Lucia ” .
W 2009 wydali czwarty album , zatytułowany `` Compañía del sol '' .
W 2011 do zespołu dołączyła wokalistka Monica Biancini , która zaśpiewała w singlu „ Chapoteo ” .
W sierpniu tego samego roku , z powodów zdrowotnych , z zespołu odszedł gitarzysta Marco Heggen .
W styczniu 2012 zespół , w nowym składzie , wyruszył w trasę koncertową po Niemczech .
W tym samym roku wydali piąty album studyjny , zatytułowany `` Bienvenido '' .
Występy w Polsce .
Zespół kilkukrotnie wystąpił z koncertami w Polsce .
28 października 2007 pojawili się w jednym z odcinków programu TVN `` Taniec z gwiazdami '' , w którym wykonali „ Vayamos compañeros ” i „ El temperamento ” .
17 listopada zagrali koncert we wrocławskim klubie Halifax .
Od 2008 regularnie występują gościnnie w teleturnieju TVP1 `` Jaka to melodia ?
`` , w którym wykonują swoje największe przeboje .
18 lipca 2008 wystąpili na koncercie `` Hity Na Czasie '' w Płocku .
