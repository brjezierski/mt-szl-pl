Marssonina betulae
Marssonina betulae ( Lib . )
Magnus – gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Grzyb mikroskopijny pasożytujący na brzozach ( `` Betula '' ) .
Wywołuje u nich chorobę zwaną czarną plamistością brzozy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudopeziza , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowała w 1832 r. Marie-Anne Libert , nadając mu nazwę `` Leptothyrium betulae '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu w 1870 r. Paul Wilhelm Magnus .
Synonimy :
Występowanie i szkodliwość .
`` Marssonina betulae '' jest pospolitym patogenem występującym na brzozach w całej Europie .
Powoduje charakterystyczne plamy na liściach i na młodych pędach .
Jego grzybnia wytwarza bezpłciowe zarodniki ( konidia ) , które wiosną i latem rozprzestrzeniają chorobę infekując liście i młode pędy .
Patogen jest bardziej szkodliwy , niż dawniej sądzono .
Powoduje osłabienie drzew , zamieranie siewek , a u dużych drzew stopniowe zamieranie korony .
W Polsce stwierdzono występowanie `` M. betulae '' na brzozie brodawkowatej ( `` Betula pendula '' ) , brzozie omszonej ( `` Betula pubescens '' ) i na niezidentyfikowanych gatunkach brzóz .
Oprócz `` M. betulae '' patogenami brzóz , często wspólnie z nim występującymi są m.in .
`` Anisogramma virgultorum '' i `` Discula betulina '' .
