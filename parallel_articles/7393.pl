Marszowice ( Wrocław )
Marszowice ( niem .
`` Marschwitz '' ) - osiedle na północno-zachodnim skraju Wrocławia , administracyjnie część osiedla Leśnica .
W granicach miasta od 1973 .
Znajduje się na północ od Leśnicy ( byłego miasta ) , na północny zachód od Stabłowic i na południowy zachód od Pracz Odrzańskich , nad rzeką Bystrzycą .
Nazwa .
Nazwa miejscowości wywodzi się prawdopodobnie od polskiej nazwy `` marszu '' .
Heinrich Adamy swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu wymienia jako pierwotną zanotowaną nazwę miejscowości `` Marschowitz '' podając jej znaczenie `` Pferderhirtendorf '' - `` Wieś wypasania koni '' , która wywodzi się najprawdopodobniej od marszów w czasie zapędzania zwierząt na pastwiska oraz spędzania ich stamtąd .
Później używana była także polska nazwa `` Marzewice '' .
