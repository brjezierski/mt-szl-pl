Martin Helwig
Martin Helwig [ warianty pisowni : `` Marcin Helwig '' , `` Martin Heilwig '' ] ( ur .
5 listopada 1516 w Nysie , zm .
26 stycznia 1574 we Wrocławiu ) – śląski pedagog i kartograf , autor pierwszej mapy Śląska .
Życiorys .
Ukończył szkoły w Nysie , studiował w Wittenberdze , gdzie uzyskał magisterium , później w 1536 roku na Akademii Krakowskiej .
Od roku 1544 pracował w Świdnicy jako kierownik szkoły miejskiej , a od 1552 we Wrocławiu , w Gimnazjum św .
Marii Magdaleny również jako jej rektor .
