Marcin Strzoda
Marcin Strzoda , , ( ur .
11 listopada 1587 w Gliwicach , zm .
26 sierpnia 1649 w Brnie ) – teolog i historyk .
Życiorys .
Marcin Strzoda był synem ławnika miejskiego w Gliwicach również Marcina Strzody i Barbary z d. Poledne .
Miał dwóch młodszych braci : Jerzego , który został sędzią w Kutnej Horze , i Andrzeja , który również został jezuitą i z czasem zyskał w Czechach uznanie jako wybitny kaznodzieja .
Pierwsze nauki pobierał w języku polskim ( pomimo obowiązującego urzędowego języka czeskiego ) w gliwickiej szkole parafialnej .
Po jej ukończeniu , dzięki pomocy wuja ( brata matki , ks .
Jerzego Poledne ) kontynuował naukę w Głogówku , poznając język niemiecki , który był tam językiem wykładowym .
W wyniku pożaru Gliwic w roku 1601 rodzina Strzodów utraciła całe mienie , popadając w biedę .
W roku 1605 , ponownie dzięki wstawiennictwu wuja , Marcin Strzoda rozpoczął studia w Seminarium Św .
Wacława w Pradze , będącego zakładem dla ubogich studentów .
Uzyskał tam opinię wzorowego ucznia .
W trzy lata później wstąpił do Towarzystwa Jezusowego w Pradze , skąd skierowano go do nowicjatu w Brnie .
