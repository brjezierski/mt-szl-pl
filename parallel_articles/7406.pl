Martynika
Martynika ( fr .
`` Martinique '' ) – departament zamorski Francji , zajmujący karaibską wyspę o tej samej nazwie , położoną w archipelagu Wysp Nawietrznych w Małych Antylach , między dwoma niezależnymi państwami Dominiką na północy i Saint Lucia na południu .
Geografia .
Martynika to górzysta wyspa pochodzenia wulkanicznego .
Spośród kilku jej wulkanów najbardziej znany a zarazem najwyższy to Montagne Pelée ( 1397 m n.p.m . ) .
Podczas erupcji 8 maja 1902 r. gorąca chmura wulkaniczna zniszczyła całkowicie nadmorskie miasto Saint-Pierre .
Zginęło wówczas 30 tysięcy osób .
Ostatni wybuch tego wulkanu miał miejsce w 1929 .
Na wyspie panuje klimat równikowy wilgotny ze średnimi temperaturami 24-28 °C .
Średnie roczne sumy opadów wynoszą od 1250 mm na wybrzeżu po 5000 mm we wnętrzu wyspy .
Na żyznych glebach wulkanicznych rosną bujne lasy równikowe , choć występują tu również gatunki drzew sprowadzone z Europy .
