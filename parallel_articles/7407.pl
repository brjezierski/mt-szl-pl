Marusza
Marusza ( węg .
`` Maros '' , rum .
`` Mureș '' ) – rzeka w zachodniej Rumunii i w południowo-wschodnich Węgrzech , lewy dopływ Cisy w zlewisku Morza Czarnego .
Długość wynosi 883 km , powierzchnia zlewni – około 30 tys .
km² .
Marusza swe źródła bierze z gór Harghita w Siedmiogrodzie ( rum .
`` Transilvania '' ) .
Później płynie przełomem między Karpatami Południowymi a Górami Zachodniorumuńskimi , a następnie wypływa na równiny Kraju Zacisańskiego i uchodzi do Cisy pod Segedynem .
Ważniejsze miasta położone nad Maruszą w Rumunii : Târgu Mureș , Alba Iulia , Arad .
