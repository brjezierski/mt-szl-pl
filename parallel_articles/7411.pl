Maria z Nazaretu ( serial telewizyjny )
Maria z Nazaretu – włosko-niemiecki telewizyjny miniserial biograficzny z 2012 roku .
Opowiada on dzieje Maryi z Nazaretu oraz świętej rodziny .
We Włoszech emitowany był jako film dwuczęściowy ( 2x100 minut ) , natomiast w Polsce jako miniserial , składający się z 4 odcinków po 50 minut każdy .
