Rów Mariański
Rów Mariański – najgłębszy znany ( ± 40 m p.p.m . )
rów oceaniczny na Ziemi , położony w zachodniej części Oceanu Spokojnego , na południowy wschód od Marianów .
Ciągnie się łukiem o długości blisko 2000 km .
Wchodzi w skład systemu rowów oceanicznych tworzących zachodnie obrzeżenie płyty pacyficznej .
Ścierają się w nim płyty pacyficzna z filipińską .
Według ostatnich pomiarów głębokość rowu wynosi m .
Historia badań .
Pierwszych pomiarów głębokości oceanu w tym miejscu dokonała ekspedycja okrętu HMS „ Challenger ” ( grudzień 1872 – maj 1876 ) , w czasie których dokonano sondowania ( za pomocą lin i dźwigów ) miejsca zwanego obecnie Głębią Challengera .
W oparciu o dwa sondowania wyznaczono głębokość na 4475 sążni ( 8184 metry ) .
Raporty z tej ekspedycji dostępne są w Internecie w ramach projektu Challenger Library .
W 1951 roku , około 75 lat po odkryciu , cały Rów Mariański łącznie z wchodzącą w jego skład Głębią Challengera był ponownie zbadany przez okręt Royal Navy również o nazwie HMS „ Challenger ” .
Głębokość zmierzono przy użyciu bardziej precyzyjnej i prostej metody – echosondy .
Pomiar wykazał wartość 5960 sążni ( metrów ) w miejscu o współrzędnych 11°19′N , 142°15′E .
W trakcie badania operator korzystał ze słuchawek i naręcznego stopera , aby ocenić czas upływający między wysłaniem sygnału a nadejściem jego echa .
