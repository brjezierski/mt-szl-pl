Maryland ( album )
Maryland - debiutancki album studyjny polskiej piosenkarki Marysi Starosty .
Wydawnictwo ukazało się 21 listopada 2008 roku nakładem wytwórni muzycznej EMI Music Poland .
Pewną popularność w Polsce zyskała pochodzą z płyty piosenka `` Nie ma nas '' , która dotarła do 4. miejsca zestawienia POPLista .
Do utworu został zrealizowany również teledysk .
Nagrania zostały wyprodukowane przez Grzegorza `` Grzecha '' Piotrowskiego i Mateusza `` Matheo '' Schmidta .
