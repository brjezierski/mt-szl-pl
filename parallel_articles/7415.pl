Marylka
Marylka – wieś w Polsce położona w województwie mazowieckim , w powiecie piaseczyńskim , w gminie Tarczyn .
Leży ok. 30 km od centrum Warszawy , ok. 15 km od siedziby powiatu Piaseczno i ok. 5 km od siedziby gminy Tarczyn .
Wieś jest siedzibą sołectwa Korzeniówka-Marylka .
W latach 1975–1998 miejscowość administracyjnie należała do województwa warszawskiego .
Wsie graniczące : Złotokłos , Ruda , Korzeniówka , Prace Małe , Kotorydz , Szczaki
