Maryno
Maryno – wieś w Polsce położona w województwie mazowieckim , w powiecie radomskim , w gminie Jedlnia-Letnisko .
W latach 1945–1975 miejscowość administracyjnie należała do województwa kieleckiego , następnie w latach 1975–1998 do województwa radomskiego .
Wieś leży na pograniczu Równiny Radomskiej i Równiny Kozienickiej .
Maryno otaczają lasy Puszczy Kozienickiej , na terenie których utworzono Kozienicki Park Krajobrazowy .
Ok. 7 km od wsi , nad brzegiem zalewu , rośnie największy w puszczy jawor , natomiast w odległości 2 km w środku lasu największe w puszczy drzewo – kilkusetletni buk , pod którym według legendy wypoczywał Tadeusz Kościuszko ( „ Buk Kościuszki ” – pomnik przyrody ) .
