María Mariño Carou
María Mariño Carou ( 1907–1967 ) – pisarka XX wieku tworząca w języku galicyjskim .
Była patronką Día das Letras Galegas w roku 2007 .
Życie .
María Mariño Carou przyszła na świat w niezamożnej rodzinie jako druga z pięciu córka Xosé Marii Mariño i Filomeny Carou .
W rodzinnej Noii chodziła do szkoły , po przeprowadzce do Boiro zaczęła uczęszczać do lokalnej biblioteki , gdzie zainteresowała się literaturą .
W 1939 poślubiła Roberta Posse Carballido .
Miała z nim jedno dziecko , które zmarło po półtora miesiąca .
Zdarzenie to wywołało u Carou problemy psychiczne i głęboką depresję .
W 1967 zapadła na białaczkę i zmarła niedługo potem .
Twórczość .
Na początku lat pięćdziesiątych , Maria Mariño Carou zaczęła pisać poematy , początkowo po hiszpańsku , następnie po galicyjsku .
Poznała poetę Uxío Novoneyra , który wypromował jej twórczość .
Wydawnictwo `` Celta '' wydało w 1963 dzieło `` Palabra no tempo '' – debiut literacki pisarki .
Utwór w krótkim czasie zyskał pochlebne opinie .
37 lat po śmierci autorki , w 1990 , Uxío Novoneyra wraz z Antonem Avilés de Taramancos wydali zbiór ostatnich utworów autorki .
Książka nosiła tytuł `` Verba que comeza '' i została napisana dwa miesiące przed śmiercią Carou .
Charakteryzowała się niezwykle osobistą treścią .
