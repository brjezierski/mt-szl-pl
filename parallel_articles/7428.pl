Masakra w Mysłowicach
Masakra w Mysłowicach – pacyfikacja górników na Górnym Śląsku przez funkcjonariuszy oddziału Grenzschutz Ost .
Jedno ze zdarzeń , które doprowadziły do wybuchu I powstania śląskiego .
15 sierpnia 1919 roku przed bramą `` Myslowitzgrube '' ok. 3 tys .
górników z żonami i dziećmi oczekiwało podjęcia wynagrodzenia za pracę , jednak dyrekcja przesuwała termin wypłaty do momentu , gdy około godziny 13 zaczęto wpuszczać na teren kopalni mniej więcej trzydziestoosobowe grupki robotników .
Wzburzony długim oczekiwaniem tłum wtargnął na plac kopalni , w związku z czym chcący ich powstrzymać oddział Grenzschutz Ost otworzył ogień .
Zabito 7 górników , 2 kobiety i 13-letniego chłopca , liczby rannych nie udało się ustalić .
Pacyfikacja wywołała ogromny szok i zradykalizowała nastroje robotników na Górnym Śląsku .
