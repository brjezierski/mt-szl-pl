Maskat
Maskat ( `` Masqaṭ '' ) – stolica i największe miasto Omanu , położone w północnej części kraju .
Liczba ludności w 2015 roku wynosiła 1,3 miliona , z czego połowa to imigranci .
Powierzchnia – 3500 km² .
Przez miasto przechodzi główna droga kraju Maskat – Salala .
Na wschód znajduje się Zatoka Omańska ( Morze Arabskie ) .
W Maskacie znajdują się wszystkie najważniejsze instytucje kraju oraz pałac sułtana Omanu .
Główną atrakcją turystyczną stolicy jest nadmorska dzielnica Matrah z tradycyjnym bazarem ( `` suk '' ) oraz dzielnica Stary Maskat , gdzie znajduje się pałac sułtana i nowo zbudowane Muzeum Narodowe .
W Maskacie urodziła się Isla Fisher , australijska aktorka .
20 kwietnia 2018 roku w Maskacie zmarł jeden z czołowych twórców muzyki elektronicznej Avicii .
