Masztalscy
Masztalscy – śląska grupa kabaretowa , założona w latach 80 .
XX wieku przez redaktorów Polskiego Radia w Katowicach – Aleksandra Trzaskę ( `` Masztalskiego '' ) i Jerzego Ciurloka ( `` Ecika '' ) .
Nazwa kabaretu pochodzi od nazwiska fikcyjnego górnika , bohatera szeregu popularnych anegdot , opowiadanych po śląsku w latach 70 .
Prawdopodobnie postać Masztalskiego wymyślił Stanisław Ligoń , a w każdym razie nie są znane wcześniejsze dowcipy z tą postacią , niż jego autorstwa .
Kabareciarze są twórcami serii książek z dowcipami wydawanych pod tytułem „ `` Klub Masztalskiego '' ” .
Łącznie Masztalscy wydali 10 książek i około 30 kaset .
Na antenie Telewizji Silesia oraz w Internecie można znaleźć program autorski Masztalskich : „ A nóm się to podobo ” .
Masztalscy występowali również za granicą , m.in .
w Niemczech , Austrii , Czechach , Słowacji , Kanadzie , Stanach Zjednoczonych i Australii .
