Matadi zostało założone wraz z portem rzecznym 1886 .
Motorem rozwoju dla miasta stało się ukończenie w 1898 linii kolejowej do stolicy kraju , Kinszasy .
Obecnie port pełni bardzo ważną rolę w gospodarce kraju , będąc terminalem przeładunkowym eksportowym i importowym kraju .
W mieście rozwinął się przemysł spożywczy , chemiczny , drzewny oraz metalurgiczny .
