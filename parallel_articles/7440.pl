Matehuala
Matehuala – miasto w Meksyku , w północnej części stanu San Luis Potosí .
Położone jest w górach Sierra Madre Wschodnia , w dolinie Salado , na wschód od Sierra Catorce .
W 2010 roku miasto liczyło 77 328 mieszkańców .
W okolicy uprawiana jest kukurydza oraz wydobywane jest złoto , srebro , ołów i miedź .
Miasto jest ośrodkiem przemysłowym ( duże huty miedzi ) .
Ponadto istnieje garbarnia .
Wytwarza się także alkohol i włókna , które są wydzielane z maguey .
Matehuala jest połączone autostradą z San Luis Potosí , stolicy stanu , w kierunku południowo-południowo-zachodnim , a także z Saltillo – stolicą stanu Coahuila .
