Matera
Matera – miasto i gmina we Włoszech , w regionie Basilicata , w prowincji Matera .
Według danych z 31 grudnia 2016 gminę zamieszkiwało 60 351 osób .
Historyczna część miasta , zwana `` Sassi '' , obejmuje unikatowe domostwa drążone w skale , jedynie z fasadami murowanymi .
W 1993 roku Materę wpisano na Listę Światowego Dziedzictwa UNESCO .
Matera była Europejską Stolicą Kultury 2019 .
W mieście znajduje się ośrodek Włoskiej Agencji Kosmicznej ( ASI ) .
Siedziba rzymskokatolickiej archidiecezji Matera-Irsina .
