Mateusz Bogusz
Mateusz Piotr Bogusz ( ur .
22 sierpnia 2001 w Rudzie Śląskiej ) – polski piłkarz występujący na pozycji pomocnika w amerykańskim klubie Los Angeles FC oraz w reprezentacji Polski do lat 21 .
Wychowanek Gwiazdy Ruda Śląska , w trakcie swojej kariery grał także w takich zespołach , jak Ruch Chorzów , Leeds United oraz UD Logroñés .
