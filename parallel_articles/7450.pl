Mauretania ( prowincja rzymska )
Mauretania – prowincja rzymska na terenie dzisiejszego północnego Maroka i północnej Algierii .
W 42 roku n.e .
cesarz Klaudiusz podzielił Mauretanię na dwie prowincje – Mauretanię Tingitana ( głównie na terenach północnego Maroka z głównymi ośrodkami administracyjnymi w Tingis i Volubilis ) oraz Mauretanię Caesariensis ze stolicą w Cezarei .
