Maurzyce
Maurzyce – wieś w Polsce , położona w województwie łódzkim , w powiecie łowickim , w gminie Zduny .
W latach 1975–1998 wieś administracyjnie należała do województwa skierniewickiego .
Wieś arcybiskupstwa gnieźnieńskiego w powiecie orłowskim województwa łęczyckiego w końcu XVI wieku .
Była wsią klucza zduńskiego arcybiskupów gnieźnieńskich .
Historia .
Miejscowość istnieje co najmniej od XIV wieku .
Pierwsze wzmianki o wsi znaleziono w średniowiecznych dokumentach Kazimierza Wielkiego , potwierdzających posiadłości arcybiskupstwa gnieźnieńskiego .
Najstarsze zapisy po łacinie podają począwszy od 1357 do lat dwudziestych XVI wieku nazwy Marzicze lub Marzice .
