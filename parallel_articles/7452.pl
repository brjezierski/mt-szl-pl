Max Glauer
Max Glauer ( ur .
11 sierpnia 1867 we Wrocławiu , zm .
27 sierpnia 1935 w Opolu ) – niemiecki i śląski artysta fotografik .
Pochodził z protestanckiej rodziny Glauerów , osiadłej we Wrocławiu .
Dzieciństwo spędził w Pokoju .
Od 1883 do co najmniej 1892 r. pracował w różnych pracowniach fotograficznych Wrocławia , ucząc się tam zawodu .
Uczył się też grafiki i malowania we wrocławskiej Królewskiej Szkole Sztuki , uzyskując jej dyplom w 1888 .
Od 1894 zamieszkał w Opolu na Krakauerstrasse 34a ( dzisiejsza ulica Krakowska ; dom już nie istnieje ) i otworzył zakład fotograficzny .
Szybko zdobył sobie uznanie zarówno wśród Opolan , jak i wśród arystokracji Cesarstwa Niemieckiego .
W 1912 król Wirtembergii Wilhelm II Wirtemberski , który był właścicielem Pokoju , mianował Glauera nadwornym fotografem .
Przez kilka lat fotografował on koronowane głowy , również cesarza Wilhelma II , kiedy ten odwiedził Mosznę .
W późniejszych latach sportretował m.in .
feldmarszałka Hindenburga , a w 1933 Adolfa Hitlera .
Często podróżował po Śląsku na rowerze , a później samochodem .
Interesowały go śląskie wsie i ich mieszkańcy .
Do dziś w niektórych śląskich domach można znaleźć zdjęcia zrobione w jego zakładzie .
W 1926 Glauer postanowił przebudować swój opolski dom - powierzył to zadanie znanemu wrocławskiemu architektowi Heinrichowi Lauterbachowi .
Dom stał się miejscem spotkań opolskiej elity intelektualnej i artystycznej , bywali tam politycy , odbywały się spotkania Opolskiej Gminy Eichendorffa .
Często odbywały się tam wystawy dzieł sztuki .
Oprócz fotografii Glauer wydał również opowiadania z czasów młodości w Pokoju , publikowane w `` Oppelner Heimatkalender '' w 1934 .
Grób artysty znajdował się na cmentarzu przy Breslauerstrasse ( ulica Wrocławska ) , lecz nie przetrwał do dzisiejszych czasów .
Pochodzenie artysty .
Według niektórych źródeł rodzina Glauera miała żydowskie korzenie .
Przeczyć temu może fakt , iż w późniejszym czasie nie był prześladowany przez nazistów , a nawet ich fotografował , nie został też pochowany na żydowskiej nekropolii .
Pojawiały się jednak informacje , że jego syn Otto , po dojściu Hitlera do władzy , został zmuszony do rezygnacji z zawodu adwokata właśnie z powodu domniemanego żydowskiego pochodzenia .
Dane rodziny Maxa Glauera przeczą żydowskiemu pochodzeniu , jak i istnieniu syna Ottona ( Max miał syna Johannesa i córkę Margareth ) .
