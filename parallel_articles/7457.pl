Mayrhofen ( Tyrol )
Mayrhofen – gmina targowa w Austrii , w kraju związkowym Tyrol , w powiecie Schwaz zamieszkałą przez 3760 mieszkańców ( stan na sierpień 2022 ) .
Leży w dolinie Zillertal , w regionie Tyrol .
Do Mayrhofen prowadzi wąskotorowa linia kolejowa z Jennbach od długości 32 km .
Sport .
Gmina oferuje możliwość uprawiania wielu sportów , zarówno zimowych jak i letnich .
Z racji tego , że leży w pobliżu lodowca Hintertux , położonego na wysokości 3250 m , na nartach można tu jeździć przez cały rok .
Na pobliski szczyt Penken prowadzi kolej gondolowa Penkenbahn .
W centrum Mayrhofen ma dolną stację jeszcze jedna kolej linowa Ahornbahn , prowadząca do kotła pod szczytem Ahornspitze .
Z miejscowości prowadzą liczne szlaki górskie .
