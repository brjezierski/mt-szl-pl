Mazowsze
Mazowsze ( ) – kraina historyczna położona w środkowym biegu Wisły oraz dorzeczu jej dopływów w centralnej oraz północno-wschodniej Polsce , znajdująca się w większości w woj .
mazowieckim ; historyczną stolicą Mazowsza jest Płock , a Mazowsza południowego Czersk , za panowania Władysława Hermana Płock był również stolicą Polski oraz najstarszym miastem tego regionu ( prawa miejskie w 1237 ) ; dzielnica historyczna Polski .
Utrzymująca się przez kilka wieków niezależność polityczna Mazowsza zadecydowała o jego odrębności regionalnej w kulturze i prawie .
