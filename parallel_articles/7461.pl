Województwo mazowieckie
Województwo mazowieckie – jednostka podziału administracyjnego Polski , największe pod względem powierzchni i ludności województwo , znajdujące się w środkowej i wschodniej części Polski .
Obejmuje obszar o powierzchni 35 558,47 km² .
Według danych z 31 grudnia 2019 r. miało około 5,4 mln mieszkańców .
Siedzibą władz województwa jest Warszawa .
Województwo zostało utworzone 1 stycznia 1999 roku .
Historia .
Historycznie województwo mazowieckie istniało w latach 1526–1795 i 1816–1837 .
