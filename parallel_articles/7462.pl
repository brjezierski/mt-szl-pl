Grodzisk Mazowiecki ( stacja kolejowa )
Grodzisk Mazowiecki – stacja kolejowa w Grodzisku Mazowieckim , w województwie mazowieckim w Polsce .
Według klasyfikacji PKP posiada kategorię dworca aglomeracyjnego .
Historia .
Była jedną z pierwszych stacji Kolei Warszawsko-Wiedeńskiej ( Droga Żelazna Warszawsko-Wiedeńska ) – linii kolejowej łączącej Warszawę z granicą zaboru austriackiego ( Galicją ) .
Pierwsza linia kolejowa na ziemiach ówczesnego Królestwa Polskiego .
Pierwszy odcinek wybudowano w 1845 .
Obecny budynek dworca wybudowano w latach 1922-1924 według projektu Romualda Millera , na miejscu zniszczonego w czasie I wojny światowej .
Od 1972 roku jest wpisany do rejestru zabytków .
