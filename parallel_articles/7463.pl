Maków Mazowiecki
Maków Mazowiecki ( w latach 20 .
`` Maków Łomżyński '' , do 1928 `` Maków nad Orzycem '' ) – miasto w województwie mazowieckim , siedziba powiatu makowskiego , położone nad rzeką Orzyc , o powierzchni 10,3 km² , 77 km na północ od Warszawy .
Znajduje się na skrzyżowaniu drogi krajowej nr 60 z drogą krajową nr 57 , trasą turystyczną z Warszawy na Pojezierze Mazurskie .
Maków uzyskał lokację miejską w 1421 roku .
Miasto królewskie Korony Królestwa Polskiego w województwie mazowieckim .
Miasto rządowe Królestwa Kongresowego , położone było w 1827 roku w powiecie pułtuskim , obwodzie pułtuskim województwa płockiego .
W mieście znajduje się zalew o powierzchni 17 ha , który stwarza wszechstronne możliwości wypoczynku , uprawiania sportów wodnych , turystyki i wędkarstwa .
W 2010 roku sanepid wydał pozytywną opinię o przydatności wody do kąpieli w makowskim zbiorniku .
