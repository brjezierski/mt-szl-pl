Mińsk Mazowiecki ( gmina wiejska )
Mińsk Mazowiecki ( daw .
`` gmina Mińsk '' ) – gmina wiejska w województwie mazowieckim , w powiecie mińskim .
W latach 1975–1998 gmina położona była w województwie siedleckim .
Gmina obejmuje przeciętnie zaludnione tereny wokół Mińska Mazowieckiego .
Miasto jest siedzibą gminy , nie będąc jej częścią .
Według danych z 22 lipca 2015 gminę zamieszkiwały 14 722 osoby .
Gospodarka .
Większość zatrudnienia zapewnia rolnictwo i praca poza gminą ( miasto Mińsk Mazowiecki , Warszawa ) .
Jedną z organizacji rolniczych jest GS `` Samopomoc Chłopska '' .
Część rozwiniętej strefy usług ( m.in .
Carrefour , Castorama , Skoda , Citroen , BP ) na zachód od Mińska Mazowieckiego przypada na miejscowość Stojadła .
W gminie znajdują się studnie głębinowe zapewniające wodę miastu .
Rozrywka .
Na terenie Gminy w Budach Janowskich znajduje się duży klub muzyczny Reaktor .
