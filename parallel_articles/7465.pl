Nowy Dwór Mazowiecki ( stacja kolejowa )
Nowy Dwór Mazowiecki – stacja kolejowa w Nowym Dworze Mazowieckim , w województwie mazowieckim , w Polsce .
Krótki opis .
Budynek dworca został oddany do użytku w 1972 roku a zaprojektowany został przez Arseniusza Romanowicza .
Przez lata wyeksploatowany i zaniedbany tak , że w 2011 roku zdecydowano o jego rozbiórce .
Od tego czasu , kasy biletowe są przeniesione do wynajmowanego lokalu w pobliżu stacji .
Stacja , przed rozbiórką obiektu , była połączona z dworcem PKS Warszawa oddział w Nowym Dworze Mazowieckim .
W planach jest budowa nowoczesnego budynku komercyjnego z częścią dworcową .
