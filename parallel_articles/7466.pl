Tomaszów Mazowiecki
Tomaszów Mazowiecki – miasto w województwie łódzkim , siedziba powiatu tomaszowskiego .
Czwarte pod względem ludności miasto w województwie i drugie z bezpłatną komunikacją miejską .
W Tomaszowie Mazowieckim znajduje się jedyny w Polsce całoroczny tor do uprawiania łyżwiarstwa szybkiego – Arena Lodowa Tomaszów Mazowiecki , od kilku lat gospodarz Pucharu Świata w tej dyscyplinie , oraz Mistrzostw Europy w „ żużlu na lodzie ” .
Jesienią w mieście odbywa się międzynarodowy festiwal Love Polish Jazz .
Jedno z siedmiu miast w Polsce z policją konną .
Według danych GUS z 31 grudnia 2021 r. miasto było zamieszkiwane przez 58 796 osób .
Toponimia .
Nazwę „ Tomaszów Mazowiecki ” miasto otrzymało wraz z prawami miejskimi w 1830 roku .
