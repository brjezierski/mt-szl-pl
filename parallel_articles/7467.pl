Ożarów Mazowiecki ( stacja kolejowa )
Ożarów Mazowiecki – stacja kolejowa w Ożarowie Mazowieckim , w województwie mazowieckim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca aglomeracyjnego .
Znajduje się tu 1 peron .
Są tu semafory świetlne .
W roku 2018 stacja obsługiwała 2–3 tys .
pasażerów na dobę .
Historia i opis .
Budynek dworca kolejowego w Ożarowie Mazowieckim powstał w 1931 .
Obecnie znajduje się pod opieką konserwatora .
Znajduje się tam oprócz kasy biletowej również sklep .
Obok budynku znajduje się peron , do którego dojść można przejściem naziemnym ( kładką ) .
Na peronie można schować się pod wiatą peronu .
Stacja ta posiada semafory świetlne .
Historycznie była to pierwsza stacja na otwartym w 1902 odcinku z Warszawy do Łowicza .
