Małastów
Małastów ( j. łemkowski Маластів ) – wieś w Polsce położona w województwie małopolskim , w powiecie gorlickim , w gminie Sękowa .
Wieś królewska starostwa bieckiego w powiecie bieckim województwa krakowskiego w końcu XVI wieku .
Położenie .
Małastów leży w dolinie potoku Małastówka .
Zabudowania położone są na wysokości 400–430 m n.p.m .
Od wschodu dominuje nad wsią grzbiet z wzniesieniami Zawiersza ( 669 m n.p.m . )
i Kornuta ( 677 m n.p.m .
) , natomiast od zachodu grzbiet biegnący od Magury Małastowskiej ( 813 m n.p.m . )
przez Ostry Dział ( 675 m n.p.m . )
ku Brusom ( 594 m n.p.m . ) .
Przez wieś biegnie droga wojewódzka nr 977 z Gorlic do granicy państwa w Koniecznej .
