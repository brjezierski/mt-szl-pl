Historia .
Małgobek powstał w 1935 roku po odkryciu pól ropy naftowej .
Wcześniej istniały tutaj osady Małgobek-Bałka ( `` Малгобек-Балка '' ) i Czeczen-Bałka ( `` Чечен-Балка '' ) .
W 1939 r. uzyskało prawa miejskie .
W czasie II wojny światowej ( Operacja Fall Blau ) , 12 września 1942 r. miejscowość zdobyta przez armię niemiecką ( Grupa Armii A ) .
3 stycznia 1943 r. została odbita przez Armię Czerwoną .
W październiku 2007 r. Małgobek został odznaczony przez prezydenta Władimira Putina Tytułem Miasta Żołnierskiej Sławy ( ros .
`` Город воинской славы '' ) za bohaterskie zatrzymanie wojsk hitlerowskich w czasie bitwy o Kaukaz .
