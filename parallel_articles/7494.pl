Małomychajliwka ( rejon synelnykowski )
Małomychajliwka ( ) – wieś na Ukrainie , w obwodzie dniepropetrowskim , w rejonie synelnykowskim .
W 2001 liczyła 3036 mieszkańców , spośród których 2947 posługiwało się językiem ukraińskim , 72 rosyjskim , 3 mołdawskim , 3 białoruskim , 1 ormiańskim , a 10 innym .
