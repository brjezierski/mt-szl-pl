Województwo małopolskie
Województwo małopolskie – jedno z 16 województw Polski .
Województwo zajmuje powierzchnię 15 182 km² i jest jednym z mniejszych w Polsce ( 12 miejsce w kraju ) .
Województwo ma około 3,4 mln mieszkańców ( stan na 31 grudnia 2019 ) , zajmując pod tym względem 4. miejsce w Polsce .
Gęstość zaludnienia jest drugą najwyższą w kraju po woj .
