Głogów Małopolski ( stacja kolejowa )
Głogów Małopolski – stacja kolejowa w Głogowie Małopolskim , w województwie podkarpackim , w Polsce .
Otwarta została 22.07.1960 .
Ruch pasażerski zawieszono w 2000 .
1.03.2007 wznowiono ruch pasażerski , początkowo pomiędzy Rzeszowem a Kolbuszową .
W roku 2017 stacja obsługiwała 20–49 pasażerów na dobę .
Stacja przeszła modernizację w roku 2010 .
Na stacji znajdują się następujące tory : jeden główny zasadniczy , dwa główne dodatkowe oraz jeden ładunkowy , sterowana jest zdalnie z Kolbuszowej .
W roku 2018 rozpoczęła się kolejna , gruntowna modernizacja , polegająca na rozbudowie istniejącego budynku dworca oraz dobudowaniu infrastruktury autobusowej celem stworzenia węzła przesiadkowego .
