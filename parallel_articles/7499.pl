Sędziszów Małopolski ( stacja kolejowa )
Sędziszów Małopolski – stacja kolejowa w Sędziszowie Małopolskim , w województwie podkarpackim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2017 stacja obsługiwała 150–199 pasażerów na dobę .
4 lutego 2019 PKP podpisały z przedsiębiorstwem Samson umowę na budowę nowego dworca wraz z infrastrukturą towarzyszącą .
