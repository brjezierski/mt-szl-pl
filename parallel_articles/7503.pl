Pomysk Mały
Pomysk Mały ( dodatkowa nazwa w j. kaszub .
Môłi Pòmësk ; niem .
`` Klein Pomeiske '' ) – wieś kaszubska w Polsce , położona w województwie pomorskim , w powiecie bytowskim , w gminie Bytów , na Pojezierzu Bytowskim , przy 228 .
Siedziba sołectwa Pomysk Mały .
w którego skład wchodzą także Leśno ( 4 mieszkańców ) i Międzygórze .
Pomysk Mały leży na trasie nieistniejącej już linii kolejowej ( Lębork-Bytów ) .
W latach 1975–1998 wieś administracyjnie należała do województwa słupskiego .
