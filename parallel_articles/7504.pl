Radawiec Mały
Radawiec Mały – wieś w Polsce położona w województwie lubelskim , w powiecie lubelskim , w gminie Konopnica .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa lubelskiego .
Wieś stanowi sołectwo gminy Konopnica .
Według Narodowego Spisu Powszechnego z roku 2011 wieś liczyła 225 mieszkańców .
Historia .
Radawiec wieś notowana w dokumentach źródłowych od 1398 jako „ Radawyecz ” w wieku IXI Radawiec Wielki , wieś i folwark , a także dobra ziemskie , i Radawiec Mały , wieś i folwark w powiecie lubelskim , gminie i parafii Konopnica , odległe 12 wiorst od Lublina .
W okolicy wsi pokłady wapienia i kamienia budowlanego , wiatrak i gorzelnia ( stan z roku 1873 ) .
Spis z 1827 roku wykazał 49 domów i 503 mieszkańców .
Długosz w opisie parafii Konopnica wylicza jako trzy oddzielne wsie : „ Radawiec Hectoris , Radawiec Puczkonis , Radawiec Pszczonczyn ” , nie podając żadnych dodatkowych szczegółów ( Długosz L.B .
t.II , s.540 ) .
Według regestru poborowego powiatu lubelskiego z roku 1531 wś Radawiecz miała w części Kacpra 6½ łanów i 1 młyn , Mateusz Puczek posiadał 1½ łana , Lodza 1½ łana , Andrzej Piotrowski ¾ łana , Spicuth 1½ ( Pawiński , Kod Małopolski , 348 ) .
