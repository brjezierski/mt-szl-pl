Trzydnik Mały
Trzydnik Mały – wieś w Polsce położona w województwie lubelskim , w powiecie kraśnickim , w gminie Trzydnik Duży .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnobrzeskiego .
Wieś stanowi sołectwo gminy Trzydnik Duży .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 430 mieszkańców .
