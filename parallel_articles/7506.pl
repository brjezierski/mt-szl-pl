Cechy morfologiczne .
Osiąga długość do 1,25 m i masę maksymalnie do 16 kg , zazwyczaj jednak przy długości 1 m waży około 6 kg .
Charakteryzuje się wydłużonym kształtem ciała i dość długim ostrym rostrum , którego względna długość może przekroczyć 50 % długości głowy .
Otwór gębowy mały , pośrodku dolnej wargi występuje wąska przerwa .
Cechą charakterystyczną sterleta jest obecność na wąsikach falban składających się z licznych wyrostków .
Posiada od 11 do 17 płytek grzbietowych z długim , ostrym grzebieniem i hakiem , około 65 małych zachodzących na siebie płytek bocznych i od 10 do 18 płytek brzusznych .
Grzbiet ma barwę szaro lub brązowozieloną , boki są jaśniejsze , brzuch jest jasnożółty lub różowobiały .
Odżywianie .
Dorosły sterlet odżywia się głównie larwami owadów ( jętek , komarów ) oraz owadami opadającymi na powierzchnię wody , a także drobnymi organizmami bentosowymi , w tym drobnymi ślimakami i pijawkami .
Zjadają też ikrę innych jesiotrów .
Rozród .
Czeczuga charakteryzuje się wczesnym osiągnięciem dojrzałości płciowej :
Płodność samic wynosi średnio od 10,6–27,4 tys .
sztuk ziaren ikry w zależności od rejonu występowania .
Warunki rzeczne :
Tarło odbywa się od maja do połowy czerwca .
U ryb dojrzałych płciowo występuje biały nalot , który jest bardziej intensywny u samców .
Inkubacja ikry trwa od 6–9 dni .
Ikra sterleta jest drobna – 2 mm .
Wylęg po resorpcji woreczka początkowo żeruje w pobliżu tarliska , a następnie rozpoczyna migracje w poszukiwaniu miejsc z większą koncentracją organizmów pokarmowych .
