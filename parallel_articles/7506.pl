Sterlet
Sterlet , czeczuga ( `` Acipenser ruthenus '' ) – gatunek słodkowodnej , wędrownej ryby z rodziny jesiotrowatych ( Acipenseridae ) .
Występowanie .
Żyje w zlewiskach Morza Czarnego , Kaspijskiego , Białego , Karskiego i Arktycznego , oraz w dorzeczu Dźwiny .
Na wschodzie areał jego występowania ograniczony jest Obem i Irtyszem .
W 1837 w Polsce podjęto nieudaną próbę introdukcji tego gatunku .
Na początku XX w. stwierdzono jego obecność w Bugu .
Prawdopodobnie przywędrował z dorzecza Dniestru Kanałem Królewskim .
W okresie międzywojennym sterlet był łowiony w południowo-wschodnich rejonach Polski w Dniestrze .
