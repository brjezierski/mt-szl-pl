Możejki ( rejon werenowski )
Możejki ( ; ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie werenowskim , w sielsowiecie Raduń .
W dwudziestoleciu międzywojennym leżały w Polsce , w województwie nowogródzkim , w powiecie lidzkim , w gminie Raduń .
Po II wojnie światowej w granicach Związku Sowieckiego .
Od 1991 w niepodległej Białorusi .
W pobliżu wsi znajduje się Wielki Kamień Możejski , będący głazem narzutowym ( eratykiem ) .
