Mchowice
Mchowice – wieś w Polsce położona w województwie łódzkim , w powiecie łęczyckim , w gminie Piątek .
Wieś archidiakona kapituły kolegiaty łęczyckiej w powiecie łęczyckim województwa łęczyckiego w końcu XVI wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa płockiego .
