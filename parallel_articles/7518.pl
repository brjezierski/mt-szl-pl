Mechelen ( 1954 )
Mechelen ( M926 ) – belgijski trałowiec z lat 50 .
XX wieku , jedna z 26 zbudowanych dla Belgijskiej Marynarki Wojennej jednostek amerykańskiego typu `` Adjutant '' .
Stępkę okrętu położono w maju 1953 roku w stoczni J. Boel & amp ; Zonen w Temse , a w skład marynarki wojennej Belgii został przyjęty 2 grudnia 1954 roku .
W 1964 roku trałowiec przebudowano na okręt badawczy .
Jednostkę wycofano ze służby w 1982 roku .
Projekt i budowa .
Projekt trałowców typu `` Adjutant '' ( `` Blackbird '' ) powstał jako rozwinięcie drugowojennych jednostek typu `` YMS '' .
Okręty miały w większości zasilić floty państw sojuszniczych , w ramach Major Defense Acquisition Program ( MDAP ) .
