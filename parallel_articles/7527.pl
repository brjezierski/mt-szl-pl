Medicine Hat
Medicine Hat – miasto w Kanadzie , w prowincji Alberta , położone nad rzeką Saskatchewan Południowy .
Jest szóstym pod względem wielkości miastem Alberty .
Znane jest z leżących pod nim dużych złóż gazu ziemnego .
Liczba mieszkańców Medicine Hat wynosi 56 997 .
Język angielski jest językiem ojczystym dla 89,7 % , francuski dla 1,0 % mieszkańców ( 2006 ) .
