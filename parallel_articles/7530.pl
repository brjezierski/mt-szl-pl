Medyń
Medyń , Medyn ( ukr .
Медин ) – wieś na Ukrainie w rejonie podwołoczyskim obwodu tarnopolskiego , około 50 km na wschód od Tarnopola .
Historia .
W rejonie miejscowości w 1920 rozegrała się bitwa pomiędzy wojskami polskimi a sowieckimi .
Od 1933 proboszczem rzymskokatolickim w Medynie był ks .
