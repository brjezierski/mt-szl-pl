Megabajt
Megabajt ( skrót MB ) – jednostka używana w informatyce oznaczająca milion bajtów .
Jednostka wykorzystywana do określania pojemności pamięci RAM , pendrive'ów , płyt CD itp .
Różnice między MB a MiB .
W informatyce przedrostek `` mega '' zwykle oznacza liczbę 1.048.576 i jest wynikiem działania 1024 × 1024 = 220 , w odróżnieniu od układu SI , gdzie 1000 × 1000 = 106 = 1.000.000 .
W celu uniknięcia nieścisłości znaczeniowych zalecane jest używanie przedrostka dwójkowego `` mebi '' ( mebibajt , skrót MiB ) , który zgodnie z propozycją IEC oznacza 1024 × 1024 .
W praktyce jednak utarła się niejednoznaczna nazwa , a brak stosowania formalnego nazewnictwa często powoduje nieporozumienia przy interpretacji pojemności pamięci .
1 MiB = 1024 KiB = 1024 · 1024 B = 1 048 576 B
1 MB = 1000 kB = 1000 · 1000 B = 1 000 000 B
