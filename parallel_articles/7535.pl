Megadeth
Megadeth – amerykańska grupa muzyczna założona w kwietniu 1983 roku w Los Angeles w Kalifornii .
Powstała z inicjatywy Dave ’ a Mustaine ’ a i Dave ’ a Ellefsona .
Nazwa zespołu to fonetyczne brzmienie angielskiego ( `` megadeath '' ) określenia hipotetycznej jednostki miar , oznaczającej liczbę miliona osób , które zginęłyby w wyniku eksplozji nuklearnej .
Formacja reprezentuje takie style muzyczne jak thrash metal , speed metal , heavy metal i metal progresywny .
Nakład ze sprzedaży wszystkich płyt grupy wynosi ponad 38 milionów egzemplarzy na całym świecie .
Zespół zdobył sześć platynowych płyt i siedem nominacji do nagród Grammy .
Należy do Wielkiej Czwórki Thrash Metalu , razem z zespołami Metallica , Slayer i Anthrax .
Otrzymali nagrodę Grammy w kategorii Best Metal Performance w 2017 roku .
Muzyka Megadeth charakteryzuje się dynamicznymi kompozycjami z bardzo szybko i agresywnie granymi riffami gitar .
Łatwo rozpoznawalny jest również śpiew Dave ’ a Mustaine ’ a .
Historia .
Początki ( 1983-1984 ) .
Wiosną 1983 roku z zespołu Metallica został wyrzucony Dave Mustaine .
Przepełniony złością muzyk postanowił założyć własny zespół .
Wkrótce Mustaine poznał przypadkiem basistę Davida Ellefsona , gitarzystę Grega Handevidta i perkusistę Dijona Carruthersa .
Muzycy założyli w Los Angeles Megadeth .
Pod koniec roku Carruthersa zastąpił Lee Rausch .
Po półrocznych poszukiwaniach wokalisty , Dave zdecydował się stanąć za mikrofonem .
W 1984 roku zespół wyruszył w pierwszą trasę koncertową , podczas której na gitarze grał Kerry King ze Slayera .
Jakiś czas później Lee Rausch został zastąpiony przez Gara Samuelsona , do Megadeth dołączył też gitarzysta Chris Poland .
