Meiringen
Meiringen – gmina ( niem .
`` Einwohnergemeinde '' ) w Szwajcarii , w kantonie Berno , w regionie administracyjnym Oberland , w okręgu Interlaken-Oberhasli .
Leży w Schweizer Voralpen .
Jest bazą narciarską i ośrodkiem sportowym .
Gmina zasłynęła po tym jak Arthur Conan Doyle opisał w jednym ze swoich opowiadań o Sherlocku Holmes'ie pobliski wodospad Reichenbach .
W gminie znajduje się muzeum Sherlocka `` ( Sherlock-Holmes-Museum ) '' .
