Mekelie
Mekelie ( `` Mek'ele '' ) – miasto w Etiopii , stolica regionu Tigraj .
Według danych projekcyjnych z roku 2021 , liczy 436,9 tys .
mieszkańców i jest trzecim co do wielkości miastem Etiopii .
Podczas klęski głodu w Etiopii w latach 1983-85 , Mekelie stało się znane z siedmiu „ obozów głodu ” wokół miasta , z 75 tysiącami uchodźców i 20 tysiącami oczekujących na wejście .
W tych siedmiu obozach codziennie umierało 50-60 osób .
