Mekka
Mekka ( arab .
مكة , `` Makka '' ) – miasto w zachodniej części Arabii Saudyjskiej , w krainie Hidżaz , u podnóża gór Dżabal al-Hidżaz , siedziba administracyjna prowincji Mekka .
W 2010 roku liczyło ok. 1,5 mln mieszkańców .
Centrum religijne islamu i najświętsze miasto muzułmanów , dostępne tylko dla wyznawców tej religii .
Według wyznawców islamu miejsce urodzenia Mahometa ( w 571 ) oraz miejsce objawienia sur mekkańskich .
Od powrotu proroka z Medyny w 630 , cel pielgrzymek wszystkich muzułmanów z całego świata .
Zarządzana przez członków rodziny kalifa Alego ( Alidów ) , od 960 noszących tytuł szarifów .
Szarifowie Mekki zachowywali niezależność od kalifatu , którą zdołali utrzymać także po włączeniu Mekki w skład imperium osmańskiego ( 1517 ) .
Od 1924 należy do Arabii Saudyjskiej .
W sensie przenośnym „ mekka ” oznacza miejsce o szczególnie dogodnych warunkach do rozwoju jakichś zainteresowań , przyciągające szeregi miłośników , hobbystów i znawców jakiejś dziedziny .
W 2018 Mekkę odwiedziło 9,73 mln turystów z całego świata – była trzynastym najczęściej odwiedzanym miastem na świecie .
Opis .
Najważniejszym miejscem w mieście jest prostokątna , ceglana budowla w środku miasta , miejsce naznaczone przez Mahometa – Al-Kaba .
Wokół tego sanktuarium wzniesiono meczet Al-Haram , mogący pomieścić jednocześnie 300 tysięcy wyznawców .
