Meksyk ( Rybnik )
Meksyk – dzielnica Rybnika położona na południe od Śródmieścia .
Na zachodzie graniczy z Zamysłowem .
Granicę z tymi dzielnicami wyznacza linia kolejowa .
Na południu Meksyk graniczy z Chwałowicami , a na wschodzie z Ligotą-Ligocką Kuźnią .
Liczy ok. 2600 mieszkańców .
Dzielnica ta powstała jako osiedle dla pracowników kolei , po których pozostały piękne i luksusowe jak na owe czasy domy .
Nazwa dzielnicy pochodzi podobno od popularnego w owych czasach powiedzenia `` Daleko jak w Meksyku '' , które odnosiło się do kraju o tej nazwie .
Ponadto daleki Meksyk był wtedy postrzegany również jako kraj dobrobytu .
Tak też kojarzyła się ówczesnym kolejowa dzielnica .
Dzielnica ma głównie charakter spokojnego osiedla domów jednorodzinnych .
Jedynie w rejonie ulicy Żołnierzy Września powstało niewielkie osiedle bloków mieszkalnych .
Głównymi ulicami przelotowymi przez dzielnicę są Chwałowicka łącząca centrum Rybnika z Chwałowicami oraz mocno zniszczona przez szkody górnicze ulica Prosta , która omijając Śródmieście łączy zachodnie i wschodnie dzielnice miasta .
Tu również znajduje się największe skrzyżowanie miasta o ruchu okrężnym , z których to Rybnik słynie .
Dzielnica ta jest objęta obecnie realizowanym projektem budowy systemu kanalizacji sanitarnej .
Najistotniejszym obiektem zlokalizowanym w dzielnicy jest Zespół Szkół Budowlanych zwany popularnie Kozimi Górami ze względu na pagórkowaty teren okolicy .
Kozie Góry to także nazwa południowej części dzielnicy .
Meksyk słynie również z rozległego parku `` Kozie Góry '' z przylegającymi ogrodami działkowymi .
