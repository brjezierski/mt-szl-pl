Przepływa przez Chiny ( ma źródła w górach Tangla na Wyżynie Tybetańskiej ) , Laos , Kambodżę oraz Wietnam .
Uchodzi do Morza Południowochińskiego szeroką deltą o powierzchni 58,5 tys .
km² .
Częściowo wyznacza granicę Laosu z Tajlandią oraz Mjanmą .
Powierzchnia dorzecza Mekongu wynosi ok. 810 tys .
km² , a długość 4500 km .
Główne dopływy to : Mun , Nam Ou , Kong i Tônlé Sab .
W górnym biegu znajduje się wiele wodospadów i progów skalnych .
Na terenie Laosu , w pobliżu granicy z Kambodżą , znajdują się jedne z największych i najszersze wodospady na świecie – Khone Phapheng .
Większe miasta leżące nad rzeką to : Wientian , Savannakhet , Phnom Penh i Luang Prabang .
Za Phnom Penh rzeka rozgałęzia się na Mekong i Tônlé Basăk , a następnie , już na terenie Wietnamu , Mekong dzieli się na sześć odnóg , a Bassac na trzy .
Łączna długość szlaków wodnych na obszarze delty wynosi ok. 3200 km .
Średni przepływ wody przy ujściu wynosi ok. 12-15 tys .
m³/s , ale może wzrosnąć do blisko 30 tys .
m³/s w sierpniu .
Wahania poziomu wód w dolnym odcinku mogą wynosić nawet 12 m. Dolina Mekongu na Równinie Kambodżańskiej pokryta jest plantacjami ryżu .
Rzeka ta niesie cenny muł , wzbogacający glebę .
Pozwala to rolnikom na tych terenach uzyskiwać rocznie aż trzy zbiory ryżu , stanowiącego tu podstawę wyżywienia .
Dolny bieg Mekongu jest najważniejszym szlakiem komunikacyjnym w Indochinach , a statki morskie dopływają do Phnom Penh , stolicy Kambodży .
Fauna Mekongu .
Rzeka ta jest siedliskiem około 1200 gatunków ryb .
Niektóre z nich , jak również krewetki , są hodowane .
W wodach Mekongu żyje także pewien zagrożony gatunek z rodziny sumowatych : pangaz ( Pangasianodon gigas ) ; ryby te mogą osiągać prawie trzy metry .
W roku 2005 rybacy złowili okaz o ciężarze 293 kg .
Żyje tu również delfin krótkogłowy ( Orcaella brevirostris ) , ssak zagrożony wymarciem , badacze przypuszczają , że w rzece zostało niespełna 100 osobników .
A od pewnej popularnej tutaj ryby z rodziny karpiowatych pochodzi nazwa kambodżańskiej jednostki monetarnej , riel kambodżański .
Rocznie poławia się do 1,3 milionów ton ryb .
