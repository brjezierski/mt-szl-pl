Charakterystyka .
Pasożyt obligatoryjny .
Jest też pasożytem dwudomowym , czyli takim , który dla pełnego cyklu rozwojowego potrzebuje dwóch gatunków roślin żywicielskich : część rozwoju odbywa na różnych gatunkach topoli ( `` Populus '' ) , a część na innych roślinach .
Jest to najmniej zbadany wśród 3 gatunków wywołujących rdzę topoli .
W polskim piśmiennictwie naukowym do 2008 r. podano tylko dwa przypadki występowania tego gatunku na topoli balsamicznej i topoli kanadyjskiej .
Gatunki podobne .
Trzy gatunki wywołujące rdzę topoli różnią się od siebie cechami morfologicznymi , mikroskopowymi , oraz gatunkami żywicieli .
