Melampsora caprearum
Melampsora caprearum Thüm .
– gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
U niektórych gatunków wierzb ( `` Salix '' ) wywołuje chorobę o nazwie rdza wierzby .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia i rozwój .
Jasnopomarańczowe ecja pojawiają się na dolnej stronie liści wierzby .
Powstają w nich kuliste lub elipsoidalne ecjospory o rozmiarach 15–25 × 12–20 μm .
Mają gęsto brodawkowaną powierzchnię i grube ściany ( 1,5–2 μm ) .
strzępki rostkowe rozproszone .
Uredinia o szerokości 1-3 mm powstają w skupiskach również na dolnej stronie liści wierzb , czasami tylko na górnej .
Powstają w nich kuliste , lub szeroko elipsoidalne urediniospory o rozmiarach 14–26 × 13–23 μm .
Mają ściany o grubości 2–3 μm , strzępki rostkowe rozproszone .
