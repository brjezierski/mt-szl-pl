Melampsora epitea
Melampsora epitea Thüm .
– gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Jest jednym z gatunków , które u wierzb ( `` Salix '' ) wywołują chorobę o nazwie rdza wierzby .
W Polsce jest gatunkiem dominującym , najczęściej wywołującym tę chorobę .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia i rozwój .
Pasożyt bezwzględny .
Jest to rdza dwudomowa , tzn , że pełny jej rozwój następuje na dwóch gatunkach żywicieli .
Drugim żywicielem jest modrzew .
Jest też rdzą pełnocyklową , tzn , że wytwarza podobnie jak rdza zbożowa wszystkie , typowe dla rdzy rodzaje zarodników .
Ecja bez perydium , na dolnej powierzchni liści , rozproszone , blado pomarańczowo-żółte , okrągłe lub podłużne , o rozmiarach 0,5-1,5 mm .
Nad nimi na górnej powierzchni liści żółte plamki .
Ecjospory okrągłe lub nieco wieloboczne , o rozmiarach 15–25 × 10–21 μm .
Mają ściany o grubości 1,5-3 μm , bez rozpoznawalnych por rostkowych .
Uredinia również na dolnej powierzchni liści , pomarańczowożółte o średnicy 0,25–2 mm średnica .
Urediniospory o kształcie od prawie kulistego do owalnego , szkliste , pomarańczowe do bladożółtych .
Mają rozmiar 12–25 × 9–19 μm i ściany o grubości 1,5–3,5 μm , bez porów rostkowych .
Wstawki główkowate , na cienkich szypułkach , czasami maczugowate , na wierzchołku rozszerzające się do 10 μm , hialinowe , o długości 35–80 μm .
Występowanie .
Występuje w Ameryce Północnej , Europie i Azji .
Na północy sięga po 78° szerokości geograficznej na Spitsbergenie , na południu po 30 stopień w Pakistanie .
Występuje także w Australii i na Nowej Zelandii .
W Europie i Polsce występuje pospolicie , W piśmiennictwie polskim podano występowanie na większości gatunków wierzb krajowych i ich mieszańcach , a także na następujących gatunkach roślin : kukułka krwista , kukułka plamista , kukułka szerokolistna , trzmielina pospolita , porzeczka alpejska , porzeczka czarna .
