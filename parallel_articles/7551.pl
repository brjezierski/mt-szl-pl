Melampsora euphorbiae
Melampsora euphorbiae ( Ficinus & amp ; C .
Schub . )
Castagne – gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny , fitopatogen , wywołujący u roślin chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1823 r. H.D.A .
Ficinus nadając mu nazwę `` Xyloma euphorbiae '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu C. Schubert i J.C. Castagne w 1843 r .
Synonimy :
Morfologia i rozwój .
Na górnej powierzchni liści roślin porażonych przez `` M. euphorbiae '' powstają jasno żółte plamy , często otoczone ciemnoczerwonym obrzeżem .
Plamy takie mogą powstawać także na łodygach i kwiatostanach .
Na dolnej powierzchni liści powstają rdzawe skupiska urediniów Mają one średnicę 0,3-0,5 mm i zawierają włosowate wstawki .
Główki wstawek mają średnicę 17,5 do 22,5 μm i bezbarwne ściany o grubości do 6,5 μm .
Powstają w nich żółtopomarańczowe urediniospory o rozmiarach 17,5-21,5 ( 26,5 ) × 15,0-19,0 μm , niemal kuliste lub elipsoidalne , o ścianach drobnokolczastych grubości 1,5 do 2,5 μm .
Pomiędzy urediniami powstają nieco później ciemnoszare telia .
Jednokomórkowe teliospory mają długość od 27 do 55 μm i szerokość od 13 do 16 μm , i jasnobrązowe ścianki o grubości od 1,0 do 1,5 μm , na wierzchołkach do 2,5 μm
Występowanie i siedlisko .
`` Melampsora euphorbiae `` jest rozprzestrzeniona na wszystkich kontynentach poza Antarktydą .
Pasożyt obligatoryjny roślin z rodzaju wilczomlecz ( `` Euphorbia '' ) .
Ponieważ niektóre gatunki tych roślin są uprawiane jako rośliny ozdobne ( zwłaszcza wilczomlecz nadobny `` Euphorbia pulcherrima '' ) , patogen ten ma znaczenie gospodarcze .
W Polsce odnotowano jego występowanie na następujących gatunkach wilczomleczy : `` Euphorbia amygdaloides .
Euphorbia angulata , Euphorbia carpatica , Euphorbia cyparissias , Euphorbia dulcis , Euphorbia esula , Euphorbia exigua , Euphorbia falcata , Euphorbia helioscopia , Euphorbia lucida , Euphorbia palustris , Euphorbia peplus , Euphorbia platyphyllos , Euphorbia serrulata '' .
