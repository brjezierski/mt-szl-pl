Melampsora laricis-populina
Melampsora laricis-populina Kleb .
– gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
U topoli ( wraz z `` Melampsora allii-populina '' i `` Melampsora populnea '' ) wywołuje chorobę o nazwie rdza topoli .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Rozwój .
Pasożyt obligatoryjny .
Jest też pasożytem dwudomowym , czyli takim , który dla pełnego cyklu rozwojowego potrzebuje dwóch gatunków roślin żywicielskich : część rozwoju odbywa na różnych gatunkach topoli ( `` Populus '' ) , a część na modrzewiach ( `` Larix '' )
Zimuje w postaci grzybni i teliospor na opadłych liściach topoli .
Wiosną teliospory kiełkują tworzą podstawki .
Na podstawkach wytwarzane są drogą płciową haploidalne bazydiospory dokonujące infekcji na liściach modrzewia .
Rozwijają się na nich spermogonia i ecja wytwarzające ecjospory .
Rozsiewane przez wiatr ecjospory infekują topole , na których rozwijają się stadia uredinialne i telialne .
Przy sprzyjającej pogodzie w czasie sezonu wegetacyjnego powstaje kilka pokoleń urediniospor rozprzestrzeniających chorobę wśród topoli .
Występowanie i siedlisko .
Opisano występowanie tego gatunku tylko w Europie .
W Polsce jest pospolity .
Stadia telialne i uredialne występują na topolach .
Choroba atakuje wszystkie gatunki i odmiany topoli , ale w różnym stopniu , niektóre są bardziej na nią odporne .
Drugim żywicielem są modrzewie .
Gatunki podobne .
Trzy gatunki wywołujące rdzę topoli różnią się od siebie cechami morfologicznymi , mikroskopowymi , oraz gatunkami żywicieli .
