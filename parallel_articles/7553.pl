Melampsora lini
Melampsora lini ( Ehrenb . )
Thüm .
– gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Wywołuje grzybową chorobę roślin o nazwie rdza lnu .
W 2012 r. gatunek ten znalazł się na 10 miejscu na liście najgroźniejszych grzybowych patogenów roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1818 r. Christian Gottfried Ehrenberg nadając mu nazwę `` Xyloma lini '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Felix von Thümen w 1878 roku .
Synonimy :
Morfologia i rozwój .
Pasożyt bezwzględny .
Jest to rdza pełnocyklowa i jednodomowa , tzn , że wytwarza wszystkie typowe dla rdzy rodzaje zarodników i przechodzi pełny cykl życiowy na jednym żywicielu .
Patogen zimuje w postaci teliospor w resztkach pożniwnych .
