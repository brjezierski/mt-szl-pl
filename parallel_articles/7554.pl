Melampsora magnusiana
Melampsora magnusiana G.H .
Wagner – gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
Jest jednym z kilku gatunków rodzaju `` Melampsora '' wywołujących rdzę topoli .
Występuje na gatunkach z sekcji `` Populus '' ( topola biała i topola osika ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonim : `` Uredo magnusiana '' ( G.H .
Wagner ) Arthur 1906 :
W literaturze naukowej `` M. magnusiana '' często wraz z trzema innymi gatunkami ( `` Melampsora pinitorqua '' , `` Melampsora larici-tremulae '' i `` Melampsora pulcherrima '' ) traktowany był jako synonim `` Melampsora populnea '' .
Aktualnie , w wyniku nowszych badań metodami biologii molekularnej , opartych o wielogenową analizę traktowane są one jako odrębne gatunki .
