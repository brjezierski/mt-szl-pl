Melampsora medusae
Melampsora medusae Thüm .
– gatunek grzybów z rzędu rdzowców ( `` Pucciniales '' ) .
U topoli wraz z `` Melampsora allii-populina '' , `` Melampsora laricis-populina '' , `` Melampsora populnea '' i jeszcze innymi gatunkami rodzaju `` Melampsora '' wywołuje chorobę o nazwie rdza topoli .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsora , Melampsoraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia .
Objawy porażenia topoli przez różne gatunki `` Melampsora '' są bardzo podobne i nie mogą być wystarczające do rozróżnienia gatunków .
Dla identyfikacji `` Melampsora medusae '' niezbędne jest zastosowanie jednej z poniższych metod :
Urediniospory są jajowate , owalne , niekiedy gruszkowate , ze zwężoną nasadą i zaokrąglonym szczytem , wewnątrz złoto-żółte .
Wymiary urediniospor to ( 23 ) 26–35 ( 37 ) x 15–19 ( 21 ) μm .
Ściana bezbarwna o grubości 1–1,5 μ m , u nasady powyżej 2 μm , w części ekwatorialnej grubsza [ 3–5 ( 5,5 ) ] μm .
Z wyjątkiem gładkiej części środkowej jest pokryta kolcami , część gładka zwykle obejmuje połowę do trzech czwartych powierzchni zarodnika .
Wstawki główkowate , o długości do 70 μm i średnicy główki 4–6 μm .
