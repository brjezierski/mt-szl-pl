Melampsoridium betulinum
Melampsoridium betulinum ( Pers . )
Kleb .
– gatunek grzybów z typu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach drzew .
Wywołuje u nich chorobę zwaną rdzą brzozy i modrzewia .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pucciniastraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo populina ß betulina '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Heinrich Klebahn w 1874 r .
Cykl życiowy i morfologia .
Jest to pasożyt dwudomowy i rdza pełnocyklowa , wytwarzająca wszystkie typowe dla rdzowców rodzaje zarodników .
