Melampsoridium hiratsukanum
Melampsoridium hiratsukanum ( Pers . )
Kleb .
– gatunek grzybów z typu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach olszy ( `` Alnus '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melampsoridium , Pucciniastraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisali go w 1927 r. Seiya Ito i N. Hiratsuka na liściach olszy szorstkiej ( `` Alnus hirsuta '' ) w Japonii .
Cykl życiowy i morfologia .
Jest to pasożyt dwudomowy i rdza pełnocyklowa , wytwarzająca wszystkie typowe dla rdzowców rodzaje zarodników .
Jego cykl rozwojowy odbywa się na olchach ( `` Alnus '' ) i modrzewiach ( `` Larix '' ) .
W Europie jednak jego cykl często skrócony jest tylko do urediniospor i teliospor występujących na olchach
Urediniospory pomarańczowe , jajowate do elipsoidalnych , zwykle o wielkości 24-32 × 12–15 μm z 4-6 porami rostkowymi rozmiarem i morfologią różnią się od rodzimego gatunku `` Melampsoridium betulinum '' .
Uredinia `` M. hiratsukanum '' są dłuższe , przypominające ostre kolce , o komórkach ostiolarnych długości do 50 μm .
Występowanie .
Początkowo `` Melampsoridium hiratsukanum '' znany był tylko w Japonii .
W latach 80 .
XX wieku pojawił się w Europie i szybko rozprzestrzenił .
W 2020 r. występuował już na znacznych obszarach Europy , pojawił się także w Ameryce Północnej i Środkowej .
W wielu krajach jest uważany za gatunek inwazyjny , również w Polsce .
W Polsce znany jest od 1999 r. Występuje na olszy czarnej ( `` Alnus glutinosa '' ) i olszy szarej ( `` Alnus incana '' ) .
Powoduje plamistość liści i osłabia rozwój drzewa .
