Melanezja
Melanezja ( z `` melas '' „ czarny ” + `` nesos '' „ wyspa ” ) – zachodnia część Oceanii .
Wyspy Melanezji są przeważnie pochodzenia wulkanicznego , rzadziej koralowego , przy czym Nowa Gwinea to wyspa kontynentalna .
Ich łączna powierzchnia wynosi 925 tys .
km² , a zamieszkuje je blisko 6,7 mln osób , głównie Papuasów i Melanezyjczyków .
