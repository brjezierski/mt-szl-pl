– gatunek grzybów z rodziny krowiakowatych ( `` Paxillaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melanogaster , Paxillaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten w 1831 roku opisał Carlo Vittadini , nadając mu nazwę `` Octaviania variegata '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Louis René Tulasne i Charles Tulasne w 1851 r .
Synonimy :
Nazwę polską nadał Feliks Teodorowicz w 1933 r .
Morfologia .
Są łatwe do identyfikacji .
Zwykle pojawiają się w dużych grupach .
Pojedynczy owocnik przeważnie ma średnicę od 2 do 6 cm , choć zdarzają się okazy mniejsze lub większe .
Jego kształt jest zmienny , od niemal kulistego do nieregularnego .
Charakterystyczne są wyrastające z jego powierzchni błyszczące ryzomorfy .
