Melbourne
Melbourne ( wym .
lub ) – miasto w południowo-wschodniej Australii położone nad zatoką Port Phillip Oceanu Indyjskiego , stolica stanu Wiktoria .
Jest drugim co do wielkości miastem Australii i Oceanii ( po Sydney ) , z liczbą ludności wynoszącą 5 mln mieszkańców .
W latach 1901–1927 stolica Australii .
Uznane przez czasopismo „ The Economist ” za „ Najlepsze miasto do życia na świecie ” w latach 2011–2017 .
W Melbourne – obok Sydney – znajduje się największe skupisko polonijne Australii .
Siedziba konsula honorowego Polski .
Toponimia .
Nazwa miasta pochodzi od posiadłości rodowej jednego z brytyjskich premierów – Williama Lamba , wicehrabiego Melbourne .
Majątek ten znajdował się w pobliżu wsi Melbourne w hrabstwie Derbyshire w Anglii .
Geografia .
Melbourne położone jest w południowo-wschodniej części kontynentu australijskiego i jest najbardziej na południe wysuniętą stolicą kontynentu .
Miasto położone jest nad zatoką Port Phillip u ujścia rzeki Yarra , której aborygeńska nazwa oznacza `` płynącą wodę '' .
We wschodniej części miasta położone są Góry Dandenong .
Klimat .
Klimat Melbourne charakteryzuje się , jako przejściowy morski z dobrze zauważalnymi wahaniami wilgotności – względnie dużymi opadami zimą ( czerwiec-sierpień ) i suchym latem ( grudzień-luty ) .
Powodem tego jest położenie Melbourne znajdującego się na pograniczu dwóch stref : gorącej śródlądowej i zimnej południowo-oceanicznej .
Dzienne różnice temperatur są najbardziej zauważalne wiosną i latem .
Spowodowane jest to tworzeniem się wtedy chłodnych frontów atmosferycznych nadchodzących od strony morza .
Są one przyczyną szeregu zjawisk takich jak : burze i wichury , duże zmiany temperatur oraz intensywne deszcze .
Występują one przelotnie .
Są często uciążliwe i znacznie obniżają temperaturę , ale też szybko mijają i bardzo szybko „ rozpogadza się ” , rośnie nasłonecznienie i z tym temperatura , które szybko ustępują na rzecz wcześniejszych opadów .
Takie wahania pogodowe zdarzają się wielokrotnie w ciągu dnia .
