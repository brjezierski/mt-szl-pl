Melissandre Fuentes
Melissandre Fuentes ( ur .
1 marca 1988 w Canillo ) – andorska łyżwiarka figurowa startująca w konkurencji solistek .
Uczestniczka mistrzostw Europy , uczestniczka mistrzostw świata juniorów oraz 4-krotna mistrzyni Andory ( 2004 , 2005 , 2007 , 2008 ) .
Jej młodsza siostra Lydia ( ur .
1990 ) również trenowała łyżwiarstwo figurowe .
