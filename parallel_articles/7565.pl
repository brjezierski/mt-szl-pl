Mielniki ( rejon grodzieński )
Mielniki ( biał .
`` Мельнікі '' , ros .
`` Мельники '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Hoża , nad Niemnem , który stanowi tu granicę państwową z Litwą .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Hoża .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 89 osób , wszystkie były wyznania rzymskokatolickiego i zadeklarowały polską przynależność narodową .
Było tu 14 budynków mieszkalnych .
Miejscowość należała do parafii świętych apostołów Piotra i Pawła w Hoży .
Wieś podlegała pod Sąd Grodzki i Okręgowy w Grodnie ; najbliższy urząd pocztowy mieścił się w Hoży .
