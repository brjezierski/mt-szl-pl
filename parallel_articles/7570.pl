Mende ( grupa etniczna )
Mende – największa grupa etniczna w Sierra Leone , obecna również w sąsiadujących państwach – Liberii i Gwinei .
Mende należą do większej grupy ludów Mande , które żyją rozproszone po całej Afryce Zachodniej .
Tradycyjnym zajęciem jest rolnictwo .
Mende posiadają także rozwinięte tradycje rzemieślnicze – głównie tkactwo , plecionkarstwo i rzeźba w drewnie .
Populację Mende szacuje się na ponad 2 miliony .
Posługują się językiem mende , z grupy językowej mande .
