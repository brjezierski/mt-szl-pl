Mendoza ( rzeka )
Mendoza - rzeka w Argentynie o długości 320 km oraz średnim przepływie 50 m³/s .
Źródła rzeki znajdują się w Andach , a uchodzi ona do jezior w bagnach Guanacache .
Jej woda wykorzystywana jest do nawadniania pól uprawnych , a także w celach energetycznych .
Główne miasto nad rzeką to Mendoza .
