Meneng ( okręg wyborczy )
Meneng jest jednym z ośmiu okręgów wyborczych Nauru .
Swoim zasięgiem obejmuje dystrykt Meneng .
Z tego okręgu wybiera się 3 członków parlamentu .
Obecnymi reprezentantami okręgu Meneng ( po wyborach z roku 2013 ) w Parlamencie Nauru są Sprent Dabwido , Tawaki Kam i Squire Jeremiah .
W przeszłości , okręg ten reprezentowali m.in .
