Mercedes ( Corrientes )
Mercedes – miasto leżące w środkowej części prowincji Corrientes w Argentynie .
W roku 2001 liczyło 35 000 mieszkańców .
Mercedes jest ważnym centrum handlowym dla regionu hodowli bydła .
Posiada też muzea : historyczne , sztuki oraz historii naturalnej , to ostatnie z ponad tysiącem eksponatów zwierząt .
10 km od miasta znajduje się sanktuarium , poświęcone pamięci miejscowego bohatera ludowego , Gauchito Gila .
