Mercurol
Mercurol – miejscowość i dawna gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Drôme .
W 2013 roku jej populacja wynosiła 2295 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia dwóch ówczesnych gmin – Mercurol oraz Veaunes – utworzono nową gminę Mercurol-Veaunes .
Siedzibą gminy została miejscowość Mercurol .
