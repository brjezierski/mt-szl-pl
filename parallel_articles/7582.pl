Osiękla kępkowa
Osiękla kępkowa ( `` Merismodes fasciculata `` ( Schwein . )
Donk ) – gatunek podstawczaków z rodziny `` Niaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Merismodes , Niaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Gatunek ten został po raz pierwszy opisany w 1832 r. jako `` Cantharellus fasciculatus '' przez L.D .
von Schweinitza .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1951 Marinus Anton Donk , przenosząc go do rodzaju `` Merismodes '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Charakterystyka .
Tworzy zazwyczaj gęste skupiska bardzo drobnych owocników .
