Mesa ( żegluga )
Mesa – pomieszczenie znajdujące się na jednostce pływającej , służące jako jadalnia , miejsce zebrań załogi , wspólnego wypoczynku , do celów reprezentacyjnych , urzędowych odpraw itp .
Na większych jednostkach mogą znajdować się oddzielna mesy oficerska , załogowa , kapitańska mesa pasażerska .
Na mniejszych jednostkach , np .
jachtach mesą nazywa się główną kabinę , nawet jeśli znajdują się w niej koje .
