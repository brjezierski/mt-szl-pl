Mesagne ( stacja kolejowa )
Mesagne ( wł .
`` Stazione di Mesagne '' ) – stacja kolejowa w Mesagne , w prowincji Brindisi , w regionie Apulia , we Włoszech .
Obecnie czynna dla pasażerów , znajduje się przy `` via Tenente Ugo Granafei '' , na północnym wjeździe do miasta z drogi ekspresowej Brindisi-Taranto , przy skrzyżowaniu Mesagne Centro-Mesagne Stazione .
Według klasyfikacji RFI ma kategorię `` brązową '' .
Opis .
Stacja Mesagne obsługuje głównie pociągi regionalne między Tarentem i Brindisi .
Rzadziej kursują pociągi Intercity ekspresowe i międzyregionalne .
Kiedyś była często wykorzystywana przez pociągi towarowe , gdyż posiada 5 torów niezelektryfikowanych .
Stacja jest częścią programu `` Pegasus '' prowadzonego przez RFI , który przewiduje przebudowę 101 stacji do roku 2016 .
Wnętrze stacji wyposażone w łazienki , poczekalnię i kasy jest w złym stanie technicznym .
Stacja obsługuje również gminy : San Donaci i San Pancrazio Salentino .
Linie kolejowe .
Tarent – Brindisi
