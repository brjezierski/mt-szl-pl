Mosiądz
Mosiądz – stop miedzi i cynku , zawierający 10–45 % cynku .
Może zawierać dodatki innych metali , takich jak ołów , glin , cyna , mangan , żelazo , chrom oraz krzem .
Topi się w temp .
poniżej 1000 °C ( zależnie od gatunku ) .
Powyżej temperatury 907 °C główny składnik stopowy mosiądzu tj .
cynk zaczyna parować powodując tworzenie się zgaru .
Mosiądz ma kolor pomarańczowożółty , przy mniejszych zawartościach cynku zbliżający się do naturalnego koloru miedzi .
Stop ten jest odporny na korozję , ciągliwy , łatwy do obróbki plastycznej .
Posiada dobre właściwości odlewnicze .
W niektórych zastosowaniach jego wadą jest stosunkowo duża gęstość ( 8,44–8,75 g/cm³ ) .
Z mosiądzu wytwarza się armaturę , osprzęt odporny na wodę morską , np .
śruby okrętowe , a także amunicję , okucia budowlane , w szczególności klamki .
Ponadto elementy maszyn w przemyśle maszynowym , samochodowym , elektrotechnicznym , okrętowym , precyzyjnym , chemicznym .
Ważnym zastosowaniem mosiądzu jest produkcja instrumentów muzycznych .
Jest on wytrzymalszy od brązu , ponieważ zawiera cynk nadający mu wytrzymałość , a co za tym idzie – twardość .
Jest on bardzo podatny na obróbkę plastyczną na zimno , np .
podczas produkcji łusek amunicji .
