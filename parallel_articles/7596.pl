Metalmania
Metalmania – coroczny festiwal muzyki metalowej i hardrockowej odbywający się od 1986 , zainaugurowany przez Tomasza Dziubińskiego , przy udziale następujących instytucji : WPWS Spodek , Śląski Jazz Club i Klub Miłośników Heavy Metal .
Obecnie festiwal organizuje wytwórnia muzyczna Metal Mind Productions , której Dziubiński jest założycielem .
Pierwsza Metalmania odbyła się w Katowicach , w hali widowiskowej „ Spodek ” .
„ Spodek ” był miejscem festiwalu do 1991 roku .
W 1991 roku festiwal odbył się w hali widowiskowo-sportowej w Jastrzębiu-Zdroju , w 1992 i 1999 w hali Baildon w Katowicach , w latach 1993-1995 odbywał się w zabrzańskim OSiR , a w 1996 w Miejskim Centrum Kultury i Sportu w Jaworznie .
W latach 1997-1998 festiwal powrócił do „ Spodka ” , od 2000 roku festiwal organizowany jest nieprzerwanie w „ Spodku ” .
Pierwsze dwa festiwale były imprezami dwudniowymi .
Trzecia edycja została podzielona na dwa koncerty w ciągu jednego dnia .
