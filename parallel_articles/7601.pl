Metan
Metan , – organiczny związek chemiczny , najprostszy węglowodór nasycony ( alkan ) .
W temperaturze pokojowej jest bezwonnym i bezbarwnym gazem .
Jest stosowany jako gaz opałowy i surowiec do syntezy wielu innych związków organicznych .
Budowa cząsteczki .
Cząsteczka metanu ma kształt tetraedru ( czworościanu foremnego ) .
Atom węgla wykazuje hybrydyzację sp3 .
Powstałe orbitale tworzą wiązania z czterema atomami wodoru .
Wszystkie te wiązania są równocenne ( kąty pomiędzy wiązaniami wynoszą 109°28 '' ' '' ) i bardzo słabo spolaryzowane , co w połączeniu z brakiem wolnych par elektronowych jest powodem względnej trwałości chemicznej tego związku .
Może on uczestniczyć tylko w typowych dla alkanów reakcjach ( np .
: spalanie ) .
Występowanie .
Metan został odkryty i wyizolowany przez Alessandro Voltę w latach 1776–1778 , gdy badał gaz z mokradeł nad jeziorem Maggiore .
Powstaje on w przyrodzie w wyniku beztlenowego rozkładu szczątków roślinnych ( np .
na bagnach ) , tworząc tzw .
gaz błotny .
Stanowi też główny składnik gazu kopalnianego i gazu ziemnego ( zwykle ≥90 % ) .
Głównym źródłem metanu jest gaz ziemny i pokłady węgla ( Główny Instytut Górnictwa wskazuje na możliwe zasoby geologiczne metanu w pokładach węgla w Polsce na poziomie 250 mld m3 ) .
Często występuje w towarzystwie innych węglowodorów , sporadycznie w towarzystwie helu i azotu .
Metan atmosferyczny .
W 2019 roku średni poziom metanu wyniósł 1874,7 ppb .
Poziom ten jest ponad dwa razy wyższy niż zmierzony kiedykolwiek w ciągu ostatnich 400 000 lat .
Historyczne stężenia metanu w ziemskiej atmosferze znajdowały się w przedziale od 300 do 800 ppb .
Metan jest gazem cieplarnianym , którego potencjał cieplarniany jest 72-krotnie większy niż dwutlenku węgla ( w skali 20 lat ) lub 25 ( w skali 100 lat ) .
Szacuje się , że w 2001 roku metan odpowiadał za 20 % wymuszenia radiacyjnego wśród gazów cieplarnianych ( wykluczając parę wodną ) .
Pomiędzy 2015 i 2019 rokiem odnotowano gwałtowny wzrost poziomu atmosferycznego metanu .
W lutym 2020 roku doniesiono , że ilość metanu emitowanego przez przemysł paliw kopalnych mógł być znacząco niedoszacowany .
