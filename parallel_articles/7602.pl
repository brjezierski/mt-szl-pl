Meudon
Meudon – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Według danych na rok 2012 gminę zamieszkiwało 45 107 osób , a gęstość zaludnienia wynosiła 4 556 osób/km² .
Wśród 1287 gmin regionu Île-de-France Meudon plasuje się na 386. miejscu pod względem powierzchni .
W miejscowości znajduje się obserwatorium astronomiczne , w którym pracował Henri-Alexandre Deslandres , francuski fizyk i astronom .
Polskim miastem partnerskim Meudon jest Ciechanów .
