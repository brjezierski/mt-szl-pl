Mexicali
Mexicali – miasto w północnym Meksyku na granicy ze Stanami Zjednoczonymi , niedaleko miasta San Diego ( USA ) .
Stolica stanu Kalifornia Dolna ( `` Baja California '' ) .
W 2010 roku liczba mieszkańców miasta wyniosła blisko 690 tys .
mieszkańców choć obszar metropolitarny liczył blisko 1,6 mln .
W mieście rozwinął się przemysł chemiczny , metalowy , włókienniczy , spożywczy oraz hutniczy .
Znajduje się tu port lotniczy , przygraniczna stacja kolejowa , dzielnica chińska `` la Chinesca '' , a nawet arena do korridy .
