Mezoameryka
Mezoameryka – region obejmujący terytoria zamieszkane przez członków wielkich środkowoamerykańskich kultur prekolumbijskich oraz ich wpływu kulturowego , ekonomicznego i politycznego .
Rozciąga się w przybliżeniu od środkowego Meksyku po Przesmyk Panamski .
