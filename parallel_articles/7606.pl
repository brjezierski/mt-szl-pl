Termin „ Mezoameryka ” został użyty po raz pierwszy przez amerykanistę Paula Kirchhoffa .
Jego zdaniem obszar ten obejmował terytorium , którego granica południowa ciągnęła się mniej więcej od ujścia rzeki Motagua do Atlantyku , poprzez jezioro Nikaragua aż do zatoki Nicoya na Pacyfiku , a północna – sięgała rzeki Panuco wpadającej do Atlantyku i rzeki Sinaloa wpadającej do Pacyfiku .
Brak kontaktu z innymi cywilizacjami sprawił , że Mezoameryka jest obszarem bardzo jednolitym pod względem kulturowym , pomimo różnic etnicznych i językowych .
