Mezowo
Mezowo ( dodatkowa nazwa w j. kaszub .
Mézowò ) – wieś kaszubska w Polsce położona w województwie pomorskim , w powiecie kartuskim , w gminie Kartuzy nad jeziorem Mezowskim w okolicy Kartuz .
Obszar wsi obejmuje również akweny jezior Okunkowo i Szczyczno .
Wieś norbertanek żukowskich w województwie pomorskim w II połowie XVI wieku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
W Mezowie urodził się Brunon Bolesław Kowalski ( 1886-1935 ) budowniczy , architekt miasta Gdyni , przedsiębiorca budowlany , radny pierwszej Rady Miasta Gdyni .
W 1936 inż .
Józef Langiewicz zaprojektował modernistyczny dom dla rodziny Kowalskich w Mezowie .
Wspomnienia wojenne z Mezowa opisał Kazimierz Kowalski ( 1937 - 2019 ) w książce Jerzego Wrzałkowskiego .
