Miami Beach
Miami Beach – miasto w Stanach Zjednoczonych , w stanie Floryda , w zespole miejskim Miami , nad Oceanem Atlantyckim .
Liczba mieszkańców w 2020 roku wynosiła 87 039 .
Centrum miasta stanowi zwarty kompleks budynków w stylu Art déco .
Miami Beach jest słynnym kurortem , a jego południowa dzielnica , South Beach , szczególnie ulubioną przez turystów z Europy , którzy zadomowili się na szeregu plaż .
Obyczaj topless jest tolerowany ( nietypowo dla USA ) w kilku miejscach .
South Beach zostało spopularyzowane w latach 80. serialem telewizyjnym `` Miami Vice '' .
Niemiecki jest trzecim najczęściej używanym językiem w South Beach , po angielskim i hiszpańskim .
