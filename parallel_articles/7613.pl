5 lipca 2021 roku podczas pobytu w Portugalii w miejscowości Tróia , gdzie Michael posiada rezydencję , zginął drugi syn pary , osiemnastoletni Emilio .
Mężczyzna zgubił trasę do domu jadąc w nocy quadem i uległ wypadkowi na drodze nieprzystosowanej do kierowania takim pojazdem .
Został wyciągnięty przez strażaków spod płonącego quada , ale nie udało się go uratować , gdyż obrażenia , jakie odniósł , były zbyt duże .
