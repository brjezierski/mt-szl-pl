Michael Ballack
Michael Ballack ( ur .
26 września 1976 w Görlitz ) – niemiecki piłkarz , grający na pozycji pomocnika .
Kariera .
Ballack zaczął uprawiać piłkę nożną w wieku 7 lat , rok później rozpoczął naukę w gimnazjum sportowym w Karl-Marx-Stadt .
W 1995 rozpoczął karierę zawodową podpisując kontrakt z klubem Chemnitzer FC , grającym w 2 .
Bundeslidze .
W 1997 przeszedł do 1 .
FC Kaiserslautern , z którym zdobył w sezonie 1997/98 tytuł mistrza Niemiec .
W sezonie 1999/00 przeszedł do Bayeru 04 Leverkusen , gdzie zajął 3. miejsce w Bundeslidze i dotarł do finału Ligi Mistrzów .
W 2002 roku przeszedł za 12,2 mln euro do Bayernu Monachium , podpisując umowę z bawarskim klubem do końca sezonu 2005/06 .
Od 15 maja 2006 został piłkarzem Chelsea , z którą podpisał wówczas 3-letni kontrakt .
Do angielskiego klubu przeszedł na tzw .
`` zasadzie wolnego transferu '' .
Za złożenie podpisu otrzymał blisko 3 mln euro .
Zgodnie z umową Ballack miał zarabiać 130 tysięcy funtów tygodniowo , czyli ponad 6 milionów funtów rocznie ( ponad 8,5 mln euro ) .
29 czerwca 2008 z reprezentacją Niemiec na boiskach w Austrii i Szwajcarii wywalczył tytuł wicemistrza Europy w piłce nożnej po przegranym finale 0-1 z Hiszpanią .
Oficjalna strona Chelsea w dniu 9 czerwca 2010 potwierdziła , że Niemiec wraz z wygaśnięciem kontraktu , opuści klub z Londynu .
Następnie Ballack na zasadzie wolnego transferu przeniósł się do Bayeru 04 Leverkusen i dołączył do tego klubu 1 lipca 2010 .
Tym samym po ośmiu latach powrócił do klubu , w którym zaczął odnosić sukcesy sportowe .
Ostatni mecz ligowy rozegrał 5 maja 2012 roku .
2 października 2012 roku poinformował o zakończeniu kariery zawodniczej .
Życie prywatne .
Michael Ballack pochodzi z miasta Görlitz .
Pół roku po jego urodzeniu , rodzice przenieśli się wraz z nim do Karl-Marx-Stadt .
15 lipca 2008 roku wziął ślub ze swoją wieloletnią partnerką Simone Lambe , z którą ma trzech synów : Louisa ( ur .
2001 ) , Emilio ( ur .
2002 , zm .
2021 ) i Jordiego ( ur .
2005 ) .
