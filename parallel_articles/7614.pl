Michael Collins ( film )
Michael Collins – film fabularny w reżyserii Neila Jordana , nakręcony w 1996 w koprodukcji amerykańsko-brytyjsko-irlandzkiej .
Dramat biograficzny o tematyce wojennej , oparty na wydarzeniach autentycznych .
Postać tytułowego bohatera odtwarza Liam Neeson , a w rolę `` Kitty Kiernan '' wcieliła się Julia Roberts .
Niektóre ze scen filmu zostały zrealizowanie w dublińskim więzieniu Kilmainham Gaol .
Film jest biografią irlandzkiego polityka i rewolucjonisty – twórcy IRA , a został oparty na faktach .
Opowiada o szczytowym okresie życia Collinsa , gdy doprowadził on do pokoju Irlandii z Anglią i do odzyskania przez nią niepodległości ( po blisko 700 latach brytyjskiej okupacji ) , poprzez podpisanie traktatu pokojowego .
Społeczeństwo irlandzkie zatwierdziło traktat , w przeciwieństwie do części rządu , która domagała się utworzenia Republiki Irlandzkiej .
W związku z tym wybuchła wojna domowa zakończona w 1922 wraz ze śmiercią Michaela Collinsa – zamordowanego w pułapce przygotowanej przez część członków IRA ( zwolenników republiki Irlandii ) .
Film kończy się cytatem Éamona de Valery – prezydenta Republiki Irlandzkiej z 1966 .
