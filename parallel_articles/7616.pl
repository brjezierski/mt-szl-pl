Michael Schumacher
Michael Schumacher , wym .
( ur .
3 stycznia 1969 w Hürth ) – niemiecki kierowca wyścigowy , w latach 1991–2006 i 2010–2012 startujący w mistrzostwach świata Formuły 1 .
Schumacher jest siedmiokrotnym mistrzem świata kierowców Formuły 1 i jest powszechnie uważany za jednego z najlepszych kierowców Formuły 1 w historii .
Schumacher jest posiadaczem takich rekordów Formuły 1 jak : najwięcej tytułów mistrza świata ( w 2020 roku rekord ten wyrównał Lewis Hamilton ) i najwięcej najszybszych okrążeń .
W 2002 roku Schumacher został jedynym kierowcą w historii Formuły 1 , który ukończył wszystkie wyścigi sezonu na podium .
Według oficjalnej strony Formuły 1 jest statystycznie drugim najlepszym kierowcą w Formule 1 ( wyprzedził go Lewis Hamilton ) .
Karierę Schumacher rozpoczynał w kartingu .
