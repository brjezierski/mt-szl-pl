Michaił Gorbaczow
Michaił Siergiejewicz Gorbaczow ( ; ur .
2 marca 1931 we wsi Priwolnoje , zm .
30 sierpnia 2022 w Moskwie ) – radziecki i rosyjski polityk .
Sekretarz Generalny Komitetu Centralnego Komunistycznej Partii Związku Radzieckiego ( 1985–1991 ) oraz przewodniczący Prezydium Rady Najwyższej ZSRR ( 1988–1990 ) .
Pierwszy i jedyny prezydent ZSRR ( 1990–1991 ) .
Człowiek Roku tygodnika „ Time ” w 1987 i 1989 , laureat Pokojowej Nagrody Nobla w 1990 .
Życiorys .
Wczesne lata .
Urodził się 2 marca 1931 we wsi Priwolnoje w Kraju Stawropolskim .
Był synem Rosjanina Siergieja Andriejewicza Gorbaczowa ( 1909–1976 ) , mechanizatora w miejscowej stacji maszyn i traktorów , oraz Marii Pantielejewnej z domu Gonkało ( wzgl .
Mariny , 1911–1995 ) , pracownicy kołchozu , z pochodzenia Ukrainki – jej rodzina wywodziła się z Czernihowa .
Przy narodzinach nadano mu imię Wiktor , jednakże wkrótce potem podczas chrztu w cerkwi w miejscowości Letnickoje jego dziadek ze strony ojca , współzałożyciel kołchozu w Priwolnoje , Andriej Moisiejewicz Gorbaczow , z niewyjaśnionych przyczyn zmienił imię dziecka na Michaił , które następnie zachowano .
W następstwie ataku Niemiec na ZSRR , w sierpniu 1941 ojciec Gorbaczowa został powołany do Armii Czerwonej , gdzie jako dowódca oddziału saperów brał udział w wielu znanych bitwach tzw .
`` wielkiej wojny ojczyźnianej '' .
Pod koniec wojny został raniony odłamkiem w nogę , zaś po niej odznaczono go Medalem „ Za Odwagę ” i dwoma Orderami Czerwonej Gwiazdy .
Przed pójściem do szkoły podstawowej Michaił Gorbaczow mieszkał głównie u dziadków ze strony matki , Pantieleja i Wasilisy Gonkało .
W szkole dobrze się uczył , wykazywał zainteresowanie wiedzą i nowościami .
Uczestniczył również w szkolnych występach artystycznych .
Od 1946 pracował także jako mechanik w ośrodku mechanizacji ( maszynowo-traktorowym ) .
Był uczony przez ojca obsługi kombajnu .
W 1949 za pracę wraz z ojcem na kombajnie został nagrodzony Orderem Czerwonego Sztandaru Pracy .
Ukończył naukę w szkole średniej ze srebrnym medalem zdając maturę w 1950 .
Podjął studia prawnicze na Uniwersytecie Moskiewskim im .
M.W .
Łomonosowa , które ukończył w 1955 .
Był pierwszym wśród przywódców ZSRR , który ukończył regularne studia uniwersyteckie .
W 1952 wstąpił do Komunistycznej Partii Związku Radzieckiego .
Po śmierci Józefa Stalina ( 5 marca 1953 ) stał się gorącym zwolennikiem reform destalinizacyjnych przywódcy Związku Radzieckiego Nikity Chruszczowa .
Po ukończeniu studiów w 1955 kontynuował działalność partyjną w Kraju Stawropolskim .
W latach 1958–1962 był I sekretarzem Komsomołu , w latach 1966–1968 I sekretarzem Komitetu Miejskiego KPZR w Stawropolu , a w latach 1970–1978 I sekretarzem Komitetu Krajowego w Stawropolu , gdzie nadzorował budowę Wielkiego Kanału Stawropolskiego .
Zaocznie ukończył Stawropolski Instytut Rolniczy , uzyskując dyplom agronoma ekonomisty ( ekonomisty rolnego ) .
Od 1962 do 1966 zarządzał stawropolskimi kołchozami i sowchozami , dzięki czemu uznawano go za znawcę radzieckiego rolnictwa .
