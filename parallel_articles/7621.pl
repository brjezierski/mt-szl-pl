Michał Grażyński
Michał Tadeusz Grażyński właśc .
Michał Tadeusz Kurzydło ( ur .
12 maja 1890 w Gdowie , zm .
10 grudnia 1965 w Londynie ) – kapitan rezerwy piechoty Wojska Polskiego , działacz niepodległościowy , społeczny i harcerski , doktor filozofii i prawa .
Długoletni wojewoda śląski , politycznie związany z sanacją .
Dzieciństwo i młodość .
Urodził się w Gdowie na terenie ówczesnych Austro-Węgier jako siódme dziecko Michała Kurzydło i Marianny z Zastawniaków .
Jego ojciec był kierownikiem czteroklasowej szkoły ludowej , natomiast matka zmarła trzy lata po urodzeniu Michała .
W momencie urodzin nosił rodzinne nazwisko Kurzydło , które gdy miał sześć lat zmieniono pod naciskiem drugiej żony jego ojca Antoniny z domu Broniowskiej na Grażyński ( od tytułu ulubionego przez jego starszą siostrę Marię poematu Adama Mickiewicza `` Grażyna '' ) .
Studiował na Uniwersytecie Jagiellońskim , otrzymując w roku 1913 doktorat z filozofii za rozprawę na temat historii systemu monetarnego w Polsce Jagiellonów .
I wojna światowa .
9 maja 1913 rozpoczął pracę jako nauczyciel w gimnazjum w C. K. I Gimnazjum w Stanisławowie , ucząc języka polskiego i geografii .
Wkrótce po wybuchu I wojny światowej zmobilizowany do cesarskiej i królewskiej Armii jako podporucznik rezerwy .
Jesienią 1914 został ciężko ranny w walkach na froncie rosyjskim i od marca 1915 resztę wojny spędził w służbie garnizonowej w Krakowie .
