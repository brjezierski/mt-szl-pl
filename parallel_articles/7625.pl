Michałów ( obwód grodzieński )
Michałów ( biał .
`` Міхайлаўка '' , ros .
`` Михайловка '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Obuchowo , nad Niemnem .
Historia .
W dwudziestoleciu międzywojennym folwark .
Leżał w Polsce , w województwie białostockim , w powiecie grodzieńskim .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 11 osób , 3 było wyznania rzymskokatolickiego , 8 prawosławnego .
Jednocześnie 13 mieszkańców zadeklarowało polską przynależność narodową , 321 białoruską a 6 żydowska .
