Michel Leblond
Michel Leblond ( ur .
10 maja 1932 w Reims , zm .
17 grudnia 2009 tamże ) – francuski piłkarz grający na pozycji pomocnika .
W swojej karierze rozegrał 4 mecze w reprezentacji Francji i strzelił w nich 1 gola .
Kariera klubowa .
Karierę piłkarską Leblond rozpoczął w klubie Stade de Reims .
W sezonie 1949/1950 zadebiutował w nim w pierwszej lidze francuskiej .
Wraz z Reims czterokrotnie wywalczył tytuł mistrza Francji w latach 1953 , 1955 , 1958 i 1960 .
W 1958 roku zdobył też Puchar Francji .
Dwukrotnie wystąpił w finale Pucharu Mistrzów .
W 1956 roku Reims przegrało w nim 3:4 z Realem Madryt , Leblond zdobył jednego z goli .
W 1959 zespół z Francji ponownie uległ Realowi , tym razem 0:2 .
W 1961 roku Leblond odszedł ze Stade Reims do innego pierwszoligowego klubu , RC Strasbourg .
Występował w nim przez cztery lata i po sezonie 1964/1965 zakończył karierę piłkarską .
Kariera reprezentacyjna .
W reprezentacji Francji Leblond zadebiutował 30 maja 1954 roku w zremisowanym 3:3 towarzyskim meczu z Belgią .
W tym samym roku został powołany do kadry na mistrzostwa świata w Szwajcarii .
Na tym turnieju był rezerwowym i nie wystąpił w żadnym spotkania .
Od 1954 do 1957 roku rozegrał w kadrze narodowej 4 mecze i strzelił 1 gola .
W 1952 roku Leblond zagrał z kadrą olimpijską na Igrzyskach Olimpijskich w Helsinkach .
