Powstało w późnym plejstocenie ( zlodowacenie Wisconsin ) , kiedy to lodowiec , wędrujący pradawną doliną rzeki i pchający olbrzymie masy materiału skalnego i gliny , zatrzymał się , nie dochodząc do dzisiejszych wydm i utworzył potężny wał morenowy .
Topniejące wody lodowca wypełniły wyżłobioną przez lodowiec dolinę , tworząc jezioro Michigan .
W miarę wycofywania się lodowca ( późny koniec plejstocenu , początek holocenu ) jezioro zmieniało swój kształt i wielkość .
Wydmy na południowych i wschodnich brzegach jeziora powstały w holocenie , wskutek działalności silnych północnych wiatrów .
