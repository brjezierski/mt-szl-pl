Michigan ( jezioro )
Michigan ( ang .
`` Lake Michigan '' ) – jezioro w Stanach Zjednoczonych , trzecie co do wielkości w kompleksie pięciu Wielkich Jezior Ameryki Północnej i jedyne z nich leżące całkowicie na obszarze jednego kraju – USA .
Na północno-wschodnim krańcu łączy się ono z jeziorem Huron przez cieśninę Mackinac , mającą 8 km szerokości w najwęższym miejscu ; hydrologicznie oba jeziora stanowią jeden zbiornik wodny .
Od wschodu i północy do jeziora ma dostęp stan Michigan , od południa Indiana , od zachodu Illinois i Wisconsin .
Do jeziora uchodzi ok. 100 cieków wodnych , z których największą jest rzeka Grand River .
