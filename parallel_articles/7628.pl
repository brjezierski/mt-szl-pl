Michnówka
Michnówka – wieś w Polsce , położona w województwie podlaskim , w powiecie hajnowskim , w gminie Narewka .
W latach 1975–1998 wieś administracyjnie należała do województwa białostockiego .
W czerwcu 1942 wieś spacyfikowali Niemcy .
Ludność została wysiedlona a zabudowania spalono .
Michnówka liczyła wtedy 42 gospodarstwa i miała ok. 170 mieszkańców .
W strukturze kościoła rzymskokatolickiego miejscowość podlega parafii św .
Jana Chrzciciela w Narewce .
Prawosławni mieszkańcy wsi należą do parafii Świętych Apostołów Piotra i Pawła w Lewkowie Starym .
