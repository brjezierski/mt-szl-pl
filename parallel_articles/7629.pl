Michorzewo ( przystanek kolejowy )
Michorzewo - nieczynny przystanek osobowy kolei wąskotorowej w Michorzewie , w gminie Kuślin , w powiecie nowotomyskim , w województwie wielkopolskim , w Polsce .
Został otwarty w 1886 roku razem z linią z Opalenicy do Lwówka .
Linia ta została zamknięta dla ruchu pasażerskiego w 1995 roku .
W 2006 rozebrane zostało torowisko .
