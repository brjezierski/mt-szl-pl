Michał Anioł
Michał Anioł ( ) , właśc .
Michelangelo di Lodovico Buonarroti Simoni ( ur .
6 marca 1475 w Caprese , zm .
18 lutego 1564 w Rzymie ) – włoski rzeźbiarz , malarz , poeta i architekt epoki odrodzenia .
Zaliczany obok Leonarda da Vinci i Rafaela Santiego do trójki największych artystów epoki renesansu , a także uznawany za jednego z najwybitniejszych artystów w dziejach świata .
Autor między innymi fresków w kaplicy Sykstyńskiej .
Wśród rzeźb najbardziej znane są `` Pietà '' i `` Dawid '' .
Z dzieł architektonicznych – projekt kopuły bazyliki św .
Piotra .
Jego działalność artystyczna jest związana z Bolonią ( 1494–1495 ) , Florencją ( 1501–1505 , 1517–1534 ) a przede wszystkim z Rzymem ( 1496–1501 , 1505–1517 , 1534–1564 ) , gdzie twórczość artysty objęta została mecenatem papieskim .
Wykształcony we florenckim warsztacie malarza Domenico Ghirlandaio , a w dziedzinie rzeźby w pracowni Bertolda di Giovanniego .
Wbrew obiegowym opiniom to nie Michał Anioł zaprojektował mundury Gwardii Szwajcarskiej .
Młodość .
Urodził się w rodzinie Buonarroti Simoni należącej do starych florenckich rodów mieszczańskich .
Gdy miał zaledwie kilka tygodni , jego rodzina przeniosła się do Florencji .
Jego ojciec Lodovico pełnił w tym czasie obowiązki burmistrza Chiusi i Caprese .
Matka , Francesca di Neri di Miniato del Sera , zmarła 6 grudnia 1481 roku , kiedy artysta miał 6 lat .
Z tego względu młody Michał Anioł był wychowywany przez spokrewnioną rodzinę kamieniarzy , co ukierunkowało jego późniejsze zainteresowania .
Od 1 kwietnia 1488 roku rozpoczął terminowanie w pracowni Domenica Ghirlandaia , uznanego florenckiego malarza .
W okresie tym artysta poznał techniki malowania fresków , jednakże bardziej pociągała go rzeźba .
