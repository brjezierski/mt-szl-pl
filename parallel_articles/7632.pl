Mick Jagger
Sir Michael Philip ‘ Mick ’ Jagger ( ur .
26 lipca 1943 w Dartford ) – brytyjski wokalista , kompozytor , autor tekstów i lider/współzałożyciel rockowego zespołu The Rolling Stones , który ma ponad 200 milionów sprzedanych płyt w ciągu pięciu dekad swojej kariery .
Wykonawca piosenek opartych na bluesie i soulu melodyjnych i pełnych ekspresji , które sam komponuje .
Jako solista wydał pięć albumów , które cieszyły się umiarkowanym powodzeniem , próbował także , bez większego powodzenia , kariery aktorskiej .
Wraz z grupą odniósł jeden z największych sukcesów komercyjnych w historii muzyki spośród wszystkich zespołów rockowych świata .
Jagger jest współtwórcą większości repertuaru grupy wraz z gitarzystą Keithem Richardsem .
12 grudnia 2003 królowa brytyjska Elżbieta II uhonorowała go brytyjskim tytułem szlacheckim i Orderem Imperium Brytyjskiego .
W 2006 został sklasyfikowany na 15. miejscu listy 100 najlepszych wokalistów wszech czasów według Hit Parader .
