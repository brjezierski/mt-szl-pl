Microbotryum violaceum
Microbotryum violaceum ( Pers . )
G. Deml & amp ; Oberw .
– gatunek grzybów z klasy `` Microbotryomycetes '' .
Grzyb pasożytniczy porażający gatunki roślin z rodziny goździkowatych ( `` Caryophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Microbotryaceae , Microbotryales , Incertae sedis , Microbotryomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1797 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo violacea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1982 r. Günther Deml i Franz Oberwinkler , przenosząc go do rodzaju `` Microbotryum '' .
