Cykl życiowy .
`` Microbotryum violaceum '' to grzyb mikroskopijny atakujący ponad 100 gatunków roślin z rodziny goździkowatych i kilka gatunków roślin z innych rodzin .
Atakuje tylko ich kwiaty .
W porażonych kwiatach zamiast pyłku wytwarzane są teliospory tego patogenu .
Nie zabija on rośliny żywicielskiej a tylko powoduje jej sterylizację .
