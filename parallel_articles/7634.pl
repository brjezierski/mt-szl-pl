Microdochium bolleyi
Microdochium bolleyi ( R. Sprague ) de Hoog & amp ; Herm.-Nijh – gatunek grzybów należący do klasy `` Sordariomycetes '' .
Jest jednym z patogenów wywołujących chorobę o nazwie antraknoza traw .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Microdochium , Amphisphaeriaceae , Amphisphaeriales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1948 r. Roderick Sprague , nadając mu nazwę `` Gloeosporium bolleyi '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Gert Sybren de Hoog i Herm.-Nijh w 19877 r .
Synonimy :
Morfologia .
Kolonie osiągają średnicę 32–38 mm w ciągu 7 dni , są gładkie , o niewyraźnym brzegu .
Uwalniają do agaru pomarańczowy pigment , później często stają się ciemnobrązowe z powodu tworzenia się chlamydospor .
Rewert początkowo różowy , potem szarobrązowy .
Brak wysięku lub zapachu .
Zanurzone strzępki gładkie , cienkościenne , o szerokości 0,7–1,5 µm , tworzące zwartą grzybnię , pomieszaną z szerszymi ( około 3 µm ) strzępkami .
Brak strzępek powietrznych .
Aparat konidialny składa się z szerokich strzępek , które stają się lokalnie silnie podzielone i tworzą skupiska komórek konidiotwórczych znajdujących się bezpośrednio na strzępkach lub na bocznych gałęziach złożonych z 1–2 kulistych komórek .
Komórki konidiotwórcze szkliste , cienkościenne , interkalarne , cylindryczne , każda z jednym locus płodnym , częściej wolne , w tym przypadku kuliste lub półkuliste , o wymiarach 2–4,5 × 2–3,5 µm , spiczaste .
Konidia powstają sympodialnie .
Są jednokomórkowe , szkliste , gładkie , cienkościenne , w kształcie półksiężyca , 5,5–8,5 × 1,6–2,2 µm .
W starych kulturach szersze strzępki są częściowo przekształcane w łańcuchy lub skupiska brązowych , nabrzmiałych , grubościennych chlamydospor o szerokości około 6–9 µm .
