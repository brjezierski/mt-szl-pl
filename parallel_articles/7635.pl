Małozorek zielonobrązowy
Małozorek zielonobrązowy ( `` Microglossum olivaceum '' ( Pers . )
Gillet ) – gatunek grzybów należący do rodziny patyczkowatych ( `` Leotiaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Microglossum , Leotiaceae , Leotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1796 r. Christiaan Hendrik Persoon , nadając mu nazwę `` Geoglossum olivaceum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Claude-Casimir Gillet w 1879 r .
Synonimy ;
Nazwa polska na podstawie opracowania M.A .
Chmiel .
Morfologia .
Wysokość 15–30 mm , szerokość 2–5 mm , kształt maczugowaty do nieco spłaszczonego i nieregularnego .
