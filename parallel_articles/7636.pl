Małozorek tłuczkowaty
Małozorek tłuczkowaty ( `` Microglossum rufum '' ( Schwein . )
Underw . )
– gatunek grzybów z klasy patyczniaków ( `` Leotiomycetes '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Microglossum , Leotiaceae , Leotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1832 r. Lewis Schweinitz nadając mu nazwę `` Geoglossum rufum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1896 r. Lucien Marcus Underwood , przenosząc go do rodzaju `` Microglossum '' .
Niektóre synonimy naukowe :
Nazwa polska według A. Chmiel .
Morfologia .
Wytwarza buławkowate owocniki , zwykle zakończone szeroką lub spłaszczoną główką .
Owocniki mają 2-6,5 cm wysokości .
Jest w całości żółty lub pomarańczowy .
Długie i cienkie , segmentowane , gładkie , szkliste , o wymiarach 18-38 × 4-6 μm .
Występowanie .
Saprotrof .
Spotykany głównie we wschodniej Ameryce Północnej , w Europie rzadko .
W Polsce bardzo rzadki .
Podobne gatunki .
Podobnym gatunkiem jest `` Mitrula elegans '' .
