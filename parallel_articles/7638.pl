W 2014 roku prezesem przedsiębiorstwa został Satya Nadella , który zastąpił na tym stanowisku Steve ’ a Ballmera .
Od 2001 roku przedsiębiorstwo produkuje konsole do gier Xbox .
Historia .
1975–1985 : Początki .
Przedsiębiorstwo zostało założone w roku 1975 w Albuquerque w stanie Nowy Meksyk przez Billa Gatesa i Paula Allena w celu wyprodukowania i sprzedaży interpretera języka BASIC .
Pierwsze międzynarodowe biuro zostało założone 1 listopada 1978 w Japonii .
1 stycznia 1979 siedziba spółki przeniosła się do Bellevue w Waszyngtonie .
Steve Ballmer dołączył 11 czerwca 1980 , a później Bill Gates został prezesem .
DOS jest pierwszym produktem , który przyniósł spółce prawdziwy sukces .
12 sierpnia 1981 po nieudanych negocjacjach z Digital Research , IBM złożył zamówienie w Microsofcie na wersję systemu CP/M do wykorzystania w komputerach IBM PC .
Po tym Microsoft nabył klon CP/M – 86-DOS od Seattle Computer Products i
ał go PC-DOS .
Widząc dominację na rynku klonów IBM PC Microsoft postanowił rozpocząć agresywny marketing MS-DOS dla klonów IBM PC , dzięki czemu Microsoft z małego przedsiębiorstwa wyrósł na jeden z największych koncernów w branży informatycznej .
