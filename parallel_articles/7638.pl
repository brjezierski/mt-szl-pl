Microsoft
Microsoft Corporation – amerykańskie przedsiębiorstwo informatyczne .
Najbardziej znane jako producent systemów operacyjnych MS-DOS , Microsoft Windows i oprogramowania biurowego Microsoft Office .
Spółka publiczna z siedzibą w Redmond w stanie Waszyngton .
Założona w 1975 roku przez Billa Gatesa i Paula Allena .
W 2012 roku przedsiębiorstwo posiadało 10 000 patentów .
Przeciętne wynagrodzenie pracowników to 106 tys .
