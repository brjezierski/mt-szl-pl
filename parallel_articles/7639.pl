Erysiphe penicillata
Erysiphe penicillata ( Wallr . )
Link – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny wielu gatunków roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1819 r. C.K .
Wallroth nadając mu nazwę `` Alphitomorpha penicillata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1824 r. J.F .
