Występowanie .
`` Microsporum audouinii '' to antropofilny grzyb dermatofitowy .
Wraz z `` Trichophyton tonsurans '' , `` Trichophyton violaceum '' i `` Trichophyton soudanense '' wywołuje u ludzi grzybicę strzygącą .
`` Microsporum audouinii '' jest głównym patogenem powodującym tę chorobę w USA , badania hiszpańskie wykazały , że w latach 1930–1951 był bardzo powszechny w Madrycie , Saragossie , Walencji i Andaluzji , ale praktycznie zniknął w latach 60. po wprowadzeniu do jego leczenia gryzeofulwiny .
