Microsporum audouinii
Microsporum audouinii Gruby – gatunek grzyba zaliczany do rzędu `` Onygenales '' .
Grzyb chorobotwórczy , dermatofit wywołujący u ludzi grzybice skóry .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Microsporium , Arthrodermataceae , Onygenales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten zdiagnozował David Gruby w 1843 r. Ma 25 synonimów .
Są nimi wszystkie odmiany i formy .
