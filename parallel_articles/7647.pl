Mieczysław Rakowski
Mieczysław Franciszek Rakowski ( ur .
1 grudnia 1926 w Kowalewku-Folwarku , zm .
8 listopada 2008 w Warszawie ) – polski polityk komunistyczny , wiceprezes Rady Ministrów w rządzie Wojciecha Jaruzelskiego ( 1981–1985 ) , prezes Rady Ministrów w latach 1988–1989 , poseł na Sejm PRL VI , VII , VIII i IX kadencji , ostatni I sekretarz Komitetu Centralnego Polskiej Zjednoczonej Partii Robotniczej ( 1989–1990 ) .
Z zawodu historyk i dziennikarz .
Wykładowca Wyższej Szkoły Humanistyczno-Ekonomicznej w Łodzi .
Życiorys .
Młodość .
Pochodził z Wielkopolski .
Urodził się 1 grudnia 1926 .
Był synem rolnika Franciszka Rakowskiego i Marii Mazurkiewicz .
Do wybuchu wojny ukończył cztery klasy szkoły powszechnej .
Jego ojciec został rozstrzelany przez Niemców , a rodzina wysiedlona .
W okresie okupacji niemieckiej nie chodził do żadnej szkoły .
Pracował w Poznaniu jako robotnik w Zakładach Naprawczych Taboru Kolejowego .
23 stycznia 1945 zgłosił się do wojska .
W kwietniu tego samego roku został skierowany do szkoły oficerskiej .
Odbył kilkumiesięczne szkolenie w Szkole Oficerów Polityczno-Wychowawczych im .
