Mikolin
Mikolin ( niem .
`` Nikoline '' ) – wieś w Polsce , położona w województwie opolskim , w powiecie brzeskim , w gminie Lewin Brzeski .
Od roku 1975 miejscowość położona w województwie opolskim , w gminie Lewin Brzeski .
Usytuowana w malowniczych widłach rzek : ujścia Nysy Kłodzkiej do Odry ( samo ujście Nysy Kłodzkiej oddalone o 4 km od centrum wsi ) .
Obecnie atrakcją wsi jest nowo wybudowana elektrownia wodna przy progu śluzy Zawada , położonej tuż za pomnikiem poległych żołnierzy radzieckich .
Nazwa .
W księdze łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej za czasów biskupa Henryka z Wierzbna w latach 1295–1305 miejscowość wymieniona jest w zlatynizowanej formie `` Niclino '' we fragmencie `` Niclino decima more polonico '' .
Miejscowość zanotowano w części księgi zwanej Rejestr Ujazdu ( łac .
Registrum Wyasdense ) .
W 1532 roku miejscowość wzmiankowana jest jako `` Nicklyn '' .
