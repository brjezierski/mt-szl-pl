Mikoszewo ( przystanek kolejowy )
Mikoszewo – wąskotorowy przystanek osobowy w Mikoszewie , w gminie Stegna , w powiecie nowodworskim , w województwie pomorskim .
Położony jest na z Koszwał do Stegny Gdańskiej otwartej w 1905 roku .
Od 2003 na odcinku od Mikoszewa Ujścia Wisły do Stegny Gdańskiej jest prowadzony ruch pasażerski .
