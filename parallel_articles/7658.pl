Historia .
Miejscowości obecnego powiatu do 1954 roku położone były w powiecie pszczyńskim , a następnie do 1975 roku w powiecie tyskim ( z wyjątkiem Ornontowic , które w 1960 roku włączono do powiatu rybnickiego ) .
