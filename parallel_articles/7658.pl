Powiat mikołowski
Powiat mikołowski – powiat w województwie śląskim , utworzony w 1999 roku w ramach reformy administracyjnej .
Siedzibą powiatu jest miasto Mikołów .
Według danych z 31 grudnia 2019 roku powiat zamieszkiwało 98 990 osób .
Natomiast według danych z 30 czerwca 2020 roku powiat zamieszkiwało 99 211 osób .
Położenie .
Powiat mikołowski położony jest w południowo-zachodniej Polsce na granicy Wyżyny Śląsko-Krakowskiej i Kotliny Oświęcimskiej .
Administracyjnie położony w południowo-zachodniej części województwa śląskiego i Górnośląskiego Okręgu Przemysłowego ( GOP ) .
Historycznie teren powiatu leży na Ziemi Pszczyńskiej na Górnym Śląsku .
