Mikołów
Mikołów ( ) – miasto w południowej Polsce , w województwie śląskim , siedziba władz powiatu mikołowskiego , w południowej części Górnośląskiego Okręgu Przemysłowego .
Według danych z 31 grudnia 2021 roku w Mikołowie żyje 41 314 osób .
Mikołów uzyskał lokację miejską w 1276 roku , ponowne nadanie praw miejskich przed 1300 rokiem .
Położenie .
Mikołów położony jest na Wyżynie Śląskiej , graniczy bezpośrednio od wschodu z Katowicami , od południa z Tychami , gminą Wyry , Łaziskami Górnymi i Orzeszem , od zachodu z gminą Ornontowice i gminą Gierałtowice , a od północy z Rudą Śląską .
Współrzędne geograficzne centrum miasta wynoszą : 50º 10 ' szerokości geograficznej północnej i 18º 54 ' długości geograficznej wschodniej .
Pod względem historycznym Mikołów pierwotnie należał do Małopolski jako część kasztelanii bytomskiej , położonej w ziemi krakowskiej .
W 1179 kasztelanię bytomską włączono do księstwa raciborskiego i od tamtego czasu Mikołów leży na Górnym Śląsku .
W latach 1975–1998 miasto administracyjnie należało do województwa katowickiego .
Powierzchnia miasta wynosi 79,2 km² .
Miasto zajmuje 34 % powierzchni powiatu .
Według danych z 2017 r. powierzchnia lasów w Mikołowie wynosiła 2147 ha
Sąsiednie gminy : Gierałtowice , Katowice , Łaziska Górne , Ornontowice , Orzesze , Ruda Śląska , Tychy , Wyry .
