Mediolan ( wł .
`` Milano '' , łac .
`` Mediolanum '' , lomb .
`` Milan '' lub `` Milà '' ) – miasto i gmina w północnych Włoszech , stolica prowincji Mediolan i regionu Lombardia .
Położone na północno-zachodnim skraju Niziny Padańskiej pomiędzy rzekami Ticino , Adda , Pad i Alpami .
Mediolan położony jest na wysokości 122 m n.p.m .
Drugie co do wielkości miasto Włoch – po Rzymie – z liczbą mieszkańców ponad 1 mln 300 tys .
