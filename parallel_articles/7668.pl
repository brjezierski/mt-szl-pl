Milesina blechni ( Syd .
& amp ; P .
Syd . )
Arthur ex Faull – gatunek grzybów z typu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny , pasożyt obligatoryjny jodły i podrzenia żebrowca .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Milesina , Pucciniastraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1903 r. Hans Sydow nadając mu nazwę `` Melampsorella blechni '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Arthur i Faull w 1910 r .
Synonimy :
Charakterystyka .
Jest to pasożyt dwudomowy , którego żywicielami jest kilka gatunków jodeł ( jodła pospolita , wonna i grecka ) oraz paproć podrzeń żebrowiec ( `` Blechnum spicant '' ) .
Jest to rdza pełnocyklowa , wytwarzająca wszystkie 5 typowych dla rdzowców zarodników .
Na jodłach wytwarzane są zarodniki typu ecjospora i spermogonium , na żywych liściach podrzenia żebrowca urediniospory , na obumarłych teliospory , z których na wiosnę rozwijają się bazydiospory .
Te ostatnie dokonują infekcji pierwotnej na igłach jodeł .
Jodłom patogen nie wyrządza większej szkody .
Uredinia w postaci żółtych ziarenek na obydwu stronach liści podrzenia żebrowca .
Urediniospory powstają w nich począwszy od sierpnia .
Przykryte są jajowatym perydium .
Podczas deszczowej pogody uwalniają się z nich białe masy zarodników .
Uredinispory są krótkie , odlegle brodawkowane , na krótkich i grubych trzoneczkach .
Teliospory powstają na dolnej stronie obumarłych liści , pojedynczo w komórkach naskórka liścia .
Teliospory o nieregularnym kształcie , cienkościenne , zbudowane z 1–70 komórek .
