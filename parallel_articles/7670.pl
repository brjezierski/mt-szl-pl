Milicz ( stacja kolejowa )
Milicz – stacja kolejowa w Miliczu , w województwie dolnośląskim , w Polsce , na trasie linii kolejowej nr 281 : Wrocław Główny – Chojnice .
Ruch pociągów .
5 marca 2019 r. wznowiono połączenia pasażerskie na wyremontowanej linii kolejowej .
W rozkładzie jazdy 2018/2019 założono obsługę stacji przez pociągi spółki Koleje Dolnośląskie .
7 połączeń dziennie w dni robocze oraz 4 pary połączeń w weekendy kursują w ramach linii `` D7 '' Krotoszyn – Jelcz-Laskowice przez Oleśnicę i Wrocław .
Czas przejazdu między Miliczem a stacją Wrocław Główny wynosi około 90 minut .
Połączenie z miastem .
Od 26 marca 2019 r. gmina Milicz organizuje bezpłatną komunikację miejską ( `` Mała Komunikacja Miejska w Miliczu '' ) , służącą lepszemu skomunikowaniu miasta ze stacją kolejową .
Założono kursowanie w dni robocze 4 kursów mikrobusu po linii okólnej , łączącej 10 przystanków w granicach miasta .
Połączenia obsługuje miejscowa placówka terenowa PKS Wołów .
Przejazdy są bezpłatne dla pasażerów .
