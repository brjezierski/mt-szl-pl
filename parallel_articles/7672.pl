Milo ( stacja metra )
Milo – stacja metra w Katanii , położona na jedynej linii sieci .
Znajduje się na zachód od dzielnicy Borgo , między via Milo i via Bronte , z trzema wejściami ( schodami i dwiema windami ) .
Inauguracja odbyła się w dniu 30 marca 2017 , W tym samym czasie co odcinek Borgo-Nesima , natomiast otwarcie publiczne odbyło się następnego dnia .
Dominującym kolorem na stacji jest żółty .
