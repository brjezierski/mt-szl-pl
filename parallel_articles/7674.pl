Milwaukee
Milwaukee – miasto w Stanach Zjednoczonych , w południowo-wschodniej części stanu Wisconsin , ośrodek administracyjny hrabstwa Milwaukee , położone na zachodnim brzegu jeziora Michigan , u zbiegu rzek Milwaukee , Menomonee i Kinnickinnic , około 150 km na północ od Chicago .
Największe miasto stanu Wisconsin , w 2020 roku liczące 577 222 mieszkańców ; główny ośrodek obszaru metropolitalnego Milwaukee obejmującego także m.in .
miasta Waukesha , Wauwatosa , West Allis i Racine , zamieszkanego przez 1 574 731 osób ( 2020 ) .
Pierwsi biali osadnicy przybyli tu na początku XIX wieku .
Założone zostały tu trzy rywalizujące ze sobą osady , które połączone zostały w jedno miasto w 1846 roku .
Milwaukee rozwinęło się jako ośrodek przemysłowy oraz port w drugiej połowie XIX wieku .
Szczególną rolę odgrywało piwowarstwo , które rozwinęło się za sprawą niemieckich imigrantów .
W szczytowym okresie , przypadającym na lata 60 .
XX wieku , miasto liczyło około 750 000 mieszkańców .
W latach 80 .
XX wieku nastąpił stopniowy zanik przemysłu .
