Historia .
W 1889 roku , wraz z wprowadzeniem nowego podziału administracyjnego , w powiecie Ashigarakami powstała wioska Minamiashigara .
1 kwietnia 1940 wioska zdobyła status miejscowości , a 1 kwietnia 1972 roku – status miasta .
