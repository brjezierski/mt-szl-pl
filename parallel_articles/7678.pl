Minecraft
Minecraft – komputerowa gra survivalowa o otwartym świecie stworzona przez Markusa Perssona i rozwijana przez Mojang Studios .
`` Minecraft '' pozwala graczom na budowanie i niszczenie obiektów położonych w losowo generowanym świecie gry .
Gracz może atakować napotkane istoty , zbierać surowce czy wytwarzać przedmioty .
Pierwsza publiczna testowa wersja gry została wydana 17 maja 2009 roku , natomiast pełna wersja ukazała się 18 listopada 2011 roku na komputery osobiste .
Następnie wydano wersje na konsole i platformy mobilne .
Jeszcze przed oficjalną premierą gra zdobyła popularność i wiele nagród .
Co roku odbywa się konwent fanów gry – Minecon .
Sukces gry spowodował , że zaczęto sprzedawać związane z nią przedmioty np .
książki , zabawki , tematyczne klocki Lego .
Rozgrywka .
`` Minecraft '' jest grą bez określonych celów , jednak bardzo ważne jest przetrwanie .
Gracze mają nieograniczone możliwości w eksplorowaniu i modyfikowaniu świata .
Dostępne są również osiągnięcia ( , w wersji 1.12 zastąpiono je systemem postępów – ) .
Widok w grze jest przedstawiony domyślnie z perspektywy pierwszej osoby , ale istnieje możliwość zmiany na widok trzecioosobowy .
Ideą `` Minecrafta '' jest budowanie i niszczenie konstrukcji składających się z bloków .
Świat gry zbudowany jest z obiektów 3D , głównie sześcianów , ułożonych w stałej strukturze siatki reprezentujących różne bloki : trawę , ziemię , kamień , drewno , lawę czy wodę .
Gracz może rozbijać bloki , a następnie tworzyć z nich inne bloki lub przedmioty .
Pomimo tego , że gracze mogą poruszać się bez żadnych przeszkód po świecie , to bloki muszą być umieszczane w trójwymiarowej siatce .
Jedynym ograniczeniem w tworzeniu jest tylko ich wyobraźnia .
Po rozpoczęciu gry , bohater zostaje umieszczony w stale rozwijającym się i niemal nieograniczonym wirtualnym świecie , który podzielony jest na biomy .
Gracz może przemierzać tereny gry składające się z nizin , gór , lasów , jaskiń czy różnych zbiorników wodnych .
Czas w grze opiera się na cyklu dnia i nocy , trwającym około 20 prawdziwych minut .
Podczas swojej wędrówki gracz może spotkać różnorodnych bohaterów niezależnych , zwanych mobami , włączając w to zwierzęta , osadników i potwory .
Na pasywne stworzenia – takie jak krowy , świnie czy kurczaki – można polować , aby zdobyć jedzenie i materiały .
Pojawiają się one w dzień .
Dla kontrastu , stworzenia wrogie , takie jak pająki , creepery , szkielety czy zombie , pojawiają się w nocy lub w ciemnych miejscach , jak na przykład jaskinie .
Świat gry jest tworzony na bieżąco podczas jego eksplorowania używając ziarna , które otrzymuje się z aktualnej godziny zegara systemowego , jeżeli gracz nie podał własnego .
Choć istnieją ograniczenia w przemieszczaniu w górę i dół , to `` Minecraft '' pozwala na niemal nieskończoną długość i szerokość mapy .
Gra pozwala na to dzieląc świat na mniejsze części nazywane `` chunkami '' , które są tworzone lub wczytywane tylko wtedy , gdy gracz jest w pobliżu nich .
Fizyka w Minecrafcie jest często przez krytyków określana jako nierealistyczna .
Nie wpływa ona na większość trwałych bloków w grze .
Minecraft , oprócz głównego świata , oferuje jeszcze dwa wymiary : Nether i End ( nazywany też Kresem ) .
Nether to wymiar podobny do piekła , do którego gracz może się dostać przez portal .
Znajduje się tam wiele unikalnych materiałów niemożliwych do zdobycia nigdzie indziej , a także może być użyty do szybkiego przemieszczania się po normalnym świecie .
End jest wymiarem , w którym znajduje się boss nazywany Ender Dragon ( pol .
Smok Endu/Smok Kresu/Kresosmok ) .
Zabicie go powoduje uruchomienie się portalu do normalnego świata i możliwość odczytania poematu Kresu napisanego przez Juliana Gougha .
Tryby gry .
Powyższe tryby gry mogą być rozgrywane w trybie jednoosobowym , bądź wieloosobowym ( poprzez sieć ) .
W 2013 roku Microsoft uruchomił usługę prostych w obsłudze serwerów gry ( „ Minecraft Realms ” ) , których uruchomienie – w przeciwieństwie do zewnętrznych dostawców – nie wymagało żadnej wiedzy technicznej .
Produkcja .
Twórca gry `` Minecraft '' , Markus Persson , pracował wcześniej nad Wurm Online oraz przez ponad cztery lata jako deweloper gier dla king.com .
Rozwój `` Minecrafta '' rozpoczął on 10 maja 2009 roku i wkrótce później zrezygnował z pracy dla king.com , aby skoncentrować się na własnym projekcie .
Do stworzenia gry zainspirowały go takie tytuły jak `` Dwarf Fortress '' ( autorstwa Bay 12 Games ) , `` Infiniminer '' ( aut .
Zachtronics Industries ) i `` Dungeon Keeper '' ( aut .
Bullfrog Productions ) .
Po odkryciu gry `` Infiniminer '' Persson oświadczył , że właśnie taką grę chce stworzyć .
