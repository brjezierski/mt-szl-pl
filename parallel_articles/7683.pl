Mingosy ( Dmochy-Rętki )
Mingosy ( dawn .
`` Dmochy-Mingosy '' ) – część wsi Dmochy-Rętki w Polsce położona w województwie mazowieckim , w powiecie sokołowskim , w gminie Bielany .
1 I – 8 XII 1973 w gminie Mokobody w powiecie siedleckim .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
