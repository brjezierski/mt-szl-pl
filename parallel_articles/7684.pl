Minneapolis
Minneapolis ( wymowa : ) – miasto w Stanach Zjednoczonych , w stanie Minnesota .
Największe miasto tego stanu , położone nad Missisipi .
Nazywane jest „ miastem jezior ” .
Stolica hrabstwa Hennepin .
Miasto jest jednym z głównych miast aglomeracji policentrycznej – Minneapolis-Saint Paul .
Historia .
Osada założona w 1680 roku , przez misję franciszkańską .
W 1820 roku powstał Fort Snelling , a w jego pobliżu dwa osiedla : Saint Anthony założone ok. 1839 roku , prawa miejskie od 1860 i Minneapolis założone ok. 1850 roku , prawa miejskie od 1867 roku .
Obydwa miasta połączono w 1872 roku .
Obecnie Minneapolis jest głównym ośrodkiem przemysłu , handlu , nauki i kultury północno – środkowej części USA .
Uniwersytet założony w 1851 roku , ze słynną galerią sztuki , Minneapolis Institute of Art ze zbiorami malarstwa europejskiego i sztuki prekolumbijskiej , Walker Art Center ; znana orkiestra symfoniczna ( Minneapolis Symphony Orchestra , obecnie Minnesota Orchestra , założona w 1903 ) .
Gospodarka .
Minneapolis jest ośrodkiem przemysłu m.in .
maszynowego , spożywczego , elektronicznego ( komputery ) i chemicznego .
Ważna giełda zbożowa .
Węzeł komunikacji drogowej i kolejowej , port na rzece Missisipi ; port lotniczy Minneapolis-St. Paul .
