Minnesota
Minnesota ( ) – stan leżący w środkowo-zachodniej części Stanów Zjednoczonych Ameryki Północnej .
Zajmuje obszar 225 365 km² ( 12. miejsce pod względem wielkości ) , który zamieszkuje 5 706 494 osób ( 22. miejsce pod względem liczby ludności ) .
Chociaż ponad 3/4 mieszkańców stanu jest rasy białej i ma korzenie europejskie , większość stanowią osoby pochodzenia niemieckiego i skandynawskiego .
Notuje się coraz większy napływ imigrantów z Afryki , Azji i Ameryki Łacińskiej .
Blisko 60 % populacji stanu skupia się w metropolii Minneapolis–Saint Paul , tzw .
