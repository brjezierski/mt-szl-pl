Minot
Minot – miasto w Stanach Zjednoczonych , w stanie Dakota Północna , w hrabstwie Ward , położone nad rzeką Souris .
Ma 36 567 mieszkańców ( 2000 ) i jest czwartym co do wielkości miastem stanu .
Około 20 km na północ od miasta znajduje się wielka baza amerykańskich sił powietrznych , Minot Air Force Base , jedna z dwóch gdzie stacjonują bombowce B-52 .
W okolicach miasta rozmieszczone są też międzykontynentalne rakiety Minuteman III .
Urodził się tutaj amerykański raper i piosenkarz Wiz Khalifa .
