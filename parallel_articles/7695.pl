Miriam Makeba
Miriam Makeba , właśc .
Zensi Miriam Makeba ( ur .
4 marca 1932 w Prospect Township przy Johannesburgu , zm .
10 listopada 2008 w Castel Volturno ) – południowoafrykańska piosenkarka , znana także jako Mama Africa .
Życiorys .
Wychowała się w Sophiatown , przedmieściu Johannesburga .
We wczesnych latach 60 .
XX wieku , z powodów politycznych wyjechała z RPA ze swoim zespołem i osiedliła się w USA .
Stała się jednym z symboli walki z apartheidem .
Do rodzinnego kraju wróciła w 1990 po uwolnieniu z więzienia Nelsona Mandeli .
Jej pierwszym mężem był południowoafrykański trębacz Hugh Masekela .
Z powodu działalności politycznej jej drugiego męża Stokely Carmichaela w Partii Czarnych Panter , musiała opuścić USA i przeniosła się na zaproszenie prezydenta Sekou Toure do Gwinei .
W 1968 otrzymała Pokojową Nagrodę im .
Daga Hammarskjölda .
W 1988 opublikowała autobiografię pt .
`` Miriam – My Story '' .
Kariera muzyczna .
Karierę muzyczną rozpoczęła w 1953 w najsłynniejszym zespole południowoafrykańskim The Manhattan Brothers .
W Stanach Zjednoczonych początkowo współpracowała z Harrym Belafonte .
W 1966 zdobyła ( jako pierwsza Afrykanka ) nagrodę Grammy .
