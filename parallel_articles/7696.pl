Dziwlik miedziany
Dziwlik miedziany , krążniczka miedziana ( `` Miriquidica garovaglii '' ( Schaer . )
Hertel & amp ; Rambold – gatunek grzybów z rodziny `` Roccellaceae '' .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Miriquidica , Lecanoraceae , Lecanorales , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy gatunek ten opisany został w 1850 r. przez Schaerera jako `` Lecidea garovaglii '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Hertel i Rambold w 1987 r .
