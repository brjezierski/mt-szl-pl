Miroslav Klose
Miroslav Josef Klose ( ur .
9 czerwca 1978 w Opolu jako Mirosław Marian Klose ) – niemiecki piłkarz polskiego pochodzenia występujący na pozycji napastnika .
Mistrz świata z 2014 roku , wicemistrz świata 2002 , dwukrotny brązowy medalista mistrzostw świata 2006 ( król strzelców 5 goli ) i 2010 , wicemistrz Europy 2008 .
Z dorobkiem 16 goli najskuteczniejszy piłkarz w historii mistrzostw świata i z dorobkiem 71 goli najskuteczniejszy zawodnik w historii reprezentacji Niemiec .
W wywiadzie zapytany o swoje korzenie odpowiedział : `` Tu się urodziłem , tu spędziłem część dzieciństwa , tu mam rodzinę .
Poza tym , moja żona pochodzi z Polski .
Z dziećmi rozmawiamy po polsku i po niemiecku .
Jak pan słyszy mój polski jest nie najgorszy choć czasem wtrącam niemieckie słowa .
Nigdy nie zapomnę , skąd pochodzę. ” Mimo wszystko nigdy nie wyraził zainteresowania zagrania w barwach biało-czerwonych .
Kariera klubowa .
Swoją karierę piłkarską rozpoczynał w zespole młodzieżowym klubu SG Blaubach-Diedelkopf .
W 1998 r. przeniósł się do występującego w Regionallidze klubu FC 08 Homburg .
W tym zespole zadebiutował 15 listopada 1998 roku w ligowym meczu z Preußen Münster .
1 kwietnia 1999 roku w meczu z Wuppertaler SV strzelił swojego jedynego gola dla tego klubu .
W 1999 roku został zawodnikiem 1 .
FC Kaiserslautern .
Początkowo występował jedynie w rezerwach , a 1 lipca 2000 został włączony do składu pierwszej drużyny .
15 kwietnia 2000 roku w wygranym 1:0 meczu , 30. kolejki , z Eintrachtem Frankfurt , zadebiutował w Bundeslidze , zmieniając w 76. minucie spotkania Jörgena Petterssona .
Pierwszego gola dla Kaisers strzelił już w następnym sezonie – 20 października 2000 roku kiedy to w 85. minucie ligowego meczu z Werderem Brema po asyście Jeffa Strassera ustalił wynik meczu na 2:0 .
W barwach Kaiserslautern , Klose rozegrał łącznie 147 meczów , w których strzelił 53 gole i zanotował 25 asyst .
1 lipca 2004 r. za ok. 5 mln euro został zawodnikiem Werderu Brema .
14 września 2004 w przegranym 0:2 meczu Werderu z Interem , zadebiutował w Lidze Mistrzów .
W sezonie 2005/06 strzelając 25 goli został królem strzelców Bundesligi oraz zdobył z Werderem Puchar Ligi Niemieckiej .
W 2006 roku został wybrany piłkarzem roku w Niemczech .
W klubie z Bremy rozegrał łącznie 131 spotkań .
Strzelił w nich 63 bramki i zanotował 47 asyst .
Od 1 lipca 2007 do maja 2011 r. był piłkarzem Bayernu Monachium , z którym święcił największe klubowe sukcesy w swojej karierze – dwukrotnie zdobywał mistrzostwo Niemiec i Puchar Niemiec , a także raz Superpuchar Niemiec i Puchar Ligi .
Ponadto z monachijskim zespołem w sezonie 2009/10 doszedł do finału Ligi Mistrzów , gdzie jednak Bayern musiał uznać wyższość Interu .
Dla Bayernu , biorąc pod uwagę wszystkie rozgrywki , rozegrał 150 meczów , w których strzelił 53 bramki i 28 razy asystował przy trafieniach kolegów .
W 2011 r. podpisał dwuletni kontrakt z włoskim klubem S.S. Lazio .
W Serie A zadebiutował 9 września 2011 w , zremisowanym 2:2 , meczu z Milanem , zdobywając jednocześnie swoją pierwszą bramkę na tamtejszych boiskach .
5 maja 2013 w ligowym meczu z Bolonią zdobył 5 bramek , czym wyrównał osiągnięcie Roberto Pruzzo , który w sezonie 1985/1986 strzelił pięć goli dla innego rzymskiego klubu- AS Romy w spotkaniu ligowym z Avellino .
26 maja 2013 roku pokonując w finale , w derbowym meczu , drużynę Romy , zdobył z Lazio Puchar Włoch .
1 lipca 2016 po wygaśnięciu kontraktu z S.S. Lazio stał się wolnym zawodnikiem .
W barwach Lazio rozegrał łącznie 171 meczów , w których strzelił 64 bramki i zaliczył 35 asyst .
