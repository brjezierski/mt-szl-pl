Misgomyces dyschirii
Misgomyces dyschirii Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Misgomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1900 r. Roland Thaxter na owadach `` Dyschirius globosus '' i `` Dyschirus salinus '' w Anglii .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
W Polsce Tomasz Majewski opisał jego występowanie na gatunkach chrząszczy z rodziny biegaczowatych ( Carabidae ) : `` Dyschirius aeneus '' , `` Dyschirius globosus '' , `` Dyschirius obscurus '' , `` Dyschirius rifipes '' , `` Dyschirius tristis '' .
