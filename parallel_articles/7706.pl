Miszkolc
Miszkolc ( , ) – czwarte co do wielkości miasto Węgier .
Obecnie Miszkolc jest stolicą komitatu ( odpowiednik województwa ) Borsod-Abaúj-Zemplén .
W 2015 roku liczba mieszkańców Miszkolca oscylowała wokół 160 tysięcy osób .
Miszkolc położony jest w odległości 185 km od Budapesztu ( autostrada M3 ) , 340 km od Krakowa ( autostrada na Słowacji ) .
