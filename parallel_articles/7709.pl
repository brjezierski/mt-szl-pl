Mississauga
Mississauga – miasto w południowym Ontario , w regionie Peel , w części Golden Horseshoe oraz metropolii Toronto ( `` GTA – Greater Toronto Area '' ) .
Nazwa Mississauga pochodzi od nazwy plemienia Indiańskiego niegdyś zamieszkującego obecne tereny miasta .
Mississauga graniczy od wschodu z Toronto , od północy z Brampton , od zachodu z Oakville i Milton .
Południową granicę miasta tworzy wybrzeże jeziora Ontario .
Mississauga jest szóstym co do wielkości miastem Kanady .
Mississauga jest miastem młodym ( prawa miejskie otrzymała w 1974 ) , lecz najdynamiczniej rozwijającym się ośrodkiem miejskim w Kanadzie .
W latach 1991–2004 ludność miasta rosła w tempie średnio 4 % rocznie .
W Mississauga mieszka duża liczba Kanadyjczyków polskiego pochodzenia , większość z emigracji końca XX wieku .
