Missoula powstała w 1860 roku jako punkt handlowy na Mullan Road , pustynnym szlaku kolejowym między Fort Benton ( Montana ) i Walla Walla ( Waszyngton ) .
Dawniej nazywano ją `` Hellgate Village '' .
Missoula jest domem dla University of Montana , publicznego uniwersytetu badawczego .
W mieście rozwinął się przemysł drzewny , papierniczy oraz spożywczy .
