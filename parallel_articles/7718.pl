Mistrzostwa Europy w Piłce Nożnej 2020 ( składy )
Artykuł grupuje składy wszystkich reprezentacji narodowych w piłce nożnej , które wystąpią podczas mistrzostw Europy 2020 od 11 czerwca do 11 lipca 2021 roku w 11 miastach Europy .
Selekcjonerzy wszystkich 24 reprezentacji biorących udział w turnieju na 10 dni przed rozpoczęciem turnieju – do 1 czerwca 2021 roku do godz .
23:59 musieli zarejestrować 26-osobowy skład ( w tym 3 bramkarzy ) .
Zmiany w składach mogły być dokonywane w przypadku stwierdzenia przez lekarza z Komisji Medycznej UEFA kontuzji lub choroby zawodników .
Zawodnicy z pozytywnym wynikiem testu na SARS-CoV-2 lub mający bliskie kontakty z osobami z takim wynikiem testu decyzją odpowiednich organów byli odizolowani od drużyny i można było ich zastąpić przed pierwszym meczem .
W przypadku bramkarzy , którzy odnieśli kontuzję po pierwszym meczu , można było ich zastąpić , jednak potem nie mogli ponownie zostać przyjęci do składu .
1 kwietnia 2021 roku UEFA po rozmowach z selekcjonerami reprezentacji biorących udział w turnieju rozważyła możliwość powiększenia liczby składu reprezentacji ( dotychczas w składzie jednej reprezentacji było 23 zawodników ) w powodu możliwej pandemii COVID-19 w drużynie oraz w celu zmniejszenia zmęczenia zawodników spowodowanego przeciążeniem meczów w sezonie 2020/2021 .
27 kwietnia 2021 roku Komisja Rozgrywek Reprezentacji Narodowych UEFA zatwierdziła rozszerzenie składu do 26 zawodników , pod warunkiem potwierdzenia przez Komitet Wykonawczy UEFA , czego dokonała 4 maja 2021 roku .
Jednak selekcjonerzy reprezentacji zgodnie z przepisami `` ( LOTG ) '' do protokołu meczowego w ażdym mogła wskazać maksymalnie 23 zawodników ( 11 w wyjściowym składzie , 12 na ławce rezerwowych ) .
