Mistrzostwa Świata w Piłce Nożnej 2010
XIX Mistrzostwa Świata w Piłce Nożnej były rozgrywane w Republice Południowej Afryki w dniach od 11 czerwca do 11 lipca 2010 roku .
Mistrzem świata została Hiszpania , która w finale pokonała Holandię po dogrywce .
Gospodarze turnieju zajęli 3. miejsce w grupie i zostali pierwszymi w historii gospodarzami , którzy zakończyli udział w turnieju na pierwszej fazie rozgrywek .
W turnieju zadebiutowały reprezentacje Słowacji i Serbii .
Gospodarz mistrzostw .
Były to pierwsze mistrzostwa świata w piłce nożnej rozegrane na kontynencie afrykańskim .
Pierwotnie do organizacji mistrzostw zgłosiło się pięć krajów : Egipt , Libia z Tunezją , Maroko i RPA .
Tunezja wycofała się , gdy FIFA podjęła decyzję o niedopuszczeniu kandydatur podwójnych .
Libia zaś została zdyskwalifikowana przez FIFA , gdyż bez Tunezji przestała spełniać stawiane kandydatom wymogi .
15 maja 2004 roku w Zurychu komitet wykonawczy FIFA 14 głosami wybrał RPA na gospodarza mistrzostw w 2010 roku .
Maroko otrzymało 10 głosów , a Egipt nie otrzymał żadnego .
Oficjalną maskotką w kształcie lamparta zostało `` Zakumi '' .
Oficjalna prezentacja odbyła się w Johannesburgu 22 września 2008 roku .
Hymn mistrzostw to „ Waka Waka ( This Time for Africa ) ” wykonany przez Shakirę .
Terminarz .
Na 11 czerwca 2010 FIFA wyznaczyła mecz otwarcia , faza grupowa zakończyła się 25 czerwca , mecze 1/8 finału odbyły się w dniach od 26–29 czerwca , mecze ćwierćfinałowe odbywały się w dniach 2 i 3 lipca , półfinały trwały w dniach 6 i 7 lipca , mecz o 3. miejsce był 10 lipca , a finał odbył się 11 lipca .
