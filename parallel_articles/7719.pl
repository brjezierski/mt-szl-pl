Klasyfikacja końcowa .
Mistrz Świata w Piłce Nożnej 2010 ( Hiszpania ) otrzymał kwotę równą 30 milionów USD .
Przegrana drużyna z finału ( Holandia ) dostała 24 miliony USD .
Za sam udział w Mistrzostwach Świata , FIFA nagrodziła każdą federację piłkarską kwotą 1 miliona USD .
Pula nagród wynosiła 420 milionów USD .
