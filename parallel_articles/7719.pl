Kwalifikacje .
Wszystkie drużyny muszą brać udział w kwalifikacjach do Mistrzostw Świata w Południowej Afryce w 2010 roku .
Po raz pierwszy od Mistrzostw Świata 1934 , gospodarz ( RPA ) weźmie udział w kwalifikacjach , ponieważ pokrywają się one z eliminacjami Pucharu Narodów Afryki 2010 .
Jednak bez względu na wynik eliminacji gospodarze mają zagwarantowany udział w Mistrzostwach Świata .
Losowanie grup , w których odbywać się będą eliminacje do turnieju finałowego miało miejsce w Durbanie , RPA 25 listopada 2007 roku .
Z udziału w rozgrywkach wycofały się reprezentacje Bhutanu , Brunei , Erytrei , Filipin , Guamu , Laosu , Republiki Środkowoafrykańskiej oraz Wysp Świętego Tomasza i Książęcej .
Koszyki .
Po rozdzieleniu wszystkich drużyn , które awansowały na koszyki , losowanie grup odbyło się w dniu 4 grudnia 2009 w Kapsztadzie w Południowej Afryce .
Ceremonię losowania prowadził sekretarz generalny FIFA – Jérôme Valcke oraz zdobywczyni Oscara – aktorka Charlize Theron .
