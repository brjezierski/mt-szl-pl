Kwalifikacje .
Pierwsza faza eliminacji do Mistrzostw Świata 2014 w Brazylii miała być połączona z Igrzyskami Pacyfiku 2011 , jednak ostatecznie turniej ten nie został częścią eliminacji .
Koszyki .
Losowanie fazy grupowej Mistrzostw Świata w Piłce Nożnej 2014 odbyło się 6 grudnia 2013 roku o godz .
17:00 ( Czasu środkowoeuropejskiego ) w Salvador da Bahia .
Ceremonię prowadzili Rodrigo Hilbert i Fernanda Lima .
Faza grupowa .
Harmonogram spotkań został określony 20 października 2011 roku w siedzibie FIFA w Zurychu .
27 września 2012 roku , Komitet Wykonawczy FIFA ogłosił godziny rozgrywania meczów .
Spotkania fazy grupowej były rozgrywane o 13:00 , 16:00 , 17:00 , 18:00 , 19:00 i 21:00 czasu lokalnego .
Dziesięć z dwunastu miast-organizatorów znajduje się w strefie czasowej czasu zimowego , wyjątki to Cuiabá i Manaus , znajdujące się w strefie czasu zimowego .
O końcowej kolejności drużyn w każdej grupie decydują :
Jeśli dwa lub więcej zespołów mają tyle samo punktów , taki sam stosunek bramek i taką samą liczbę bramek strzelonych , kolejność ustala się w następujący sposób :
a ) liczba punktów uzyskanych w meczach między zainteresowanymi drużynami ,
b ) bilans bramek po meczach między zainteresowanymi drużynami ,
c ) liczba bramek strzelonych w meczach między zainteresowanymi drużynami ,
d ) losowanie przeprowadzone przez Komitet Wykonawczy FIFA .
Uwaga : W poniższym terminarzu turnieju podano czas lokalny .
