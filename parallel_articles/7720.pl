Mistrzostwa Świata w Piłce Nożnej 2014
XX Mistrzostwa Świata w Piłce Nożnej – rozgrywane były od 12 czerwca do 13 lipca 2014 roku w Brazylii .
Mistrzem świata po raz czwarty w swojej historii zostały Niemcy , które pokonały po dogrywce Argentynę , jednocześnie zostając pierwszą w historii europejską drużyną , która triumfowała w mistrzostwach organizowanych na kontynencie amerykańskim .
Gospodarze zajęli 4. lokatę , przegrywając w meczu o 3. miejsce z Holandią .
7 marca 2004 roku FIFA ogłosiła , że zgodnie z polityką przyznawania praw organizacji mistrzostw przemiennie federacjom piłki nożnej na różnych kontynentach , mistrzostwa w 2014 roku odbędą się w Ameryce Południowej .
17 marca 2004 r. CONMEBOL ( południowoamerykańska federacja piłki nożnej ) jednogłośnie wybrała Brazylię jako swojego jedynego kandydata , jednak pod koniec 2006 roku gotowość organizacji tych rozgrywek zgłosiła także federacja kolumbijska .
Ostatecznie jednak 13 kwietnia 2007 zrezygnowała ona z chęci organizacji Mistrzostw Świata 2014 , a więc jedyną federacją wyrażającą gotowość organizacji tych rozgrywek była federacja brazylijska .
W turnieju zadebiutowała reprezentacja Bośni i Hercegowiny .
8 lipca 2010 roku FIFA zaprezentowała oficjalne logo mistrzostw .
2 września 2012 roku ogłoszono , że oficjalna piłka mistrzostw będzie nosić nazwę Brazuca .
Została ona zaprezentowana 3 grudnia 2013 roku , na trzy dni przed losowaniem fazy grupowej turnieju .
Maskotką mistrzostw świata w Brazylii był pancernik kulowaty o imieniu Fuleco .
Gospodarz .
W dniu 30 października 2007 FIFA ogłosiła , że gospodarzem turnieju będzie Brazylia .
Była to jedyna kandydatura po wycofaniu oferty kolumbijskiej .
Brazylia zorganizowała mistrzostwa świata po raz drugi .
Pierwszy raz była gospodarzem w 1950 r .
Przygotowania .
W 2009 r. ówczesny prezydent Lula da Silva stwierdził , że rząd brazylijski nie sfinansuje budowy stadionów , jednak będzie inwestował w infrastrukturę miast , które będą organizatorami spotkań podczas turnieju .
21 stycznia 2012 szef FIFA , Joseph Blatter stwierdził , że przygotowania są na dalszym poziomie niż w Rosji , gdzie mundial odbędzie się w 2018 .
W listopadzie 2012 roku większość inwestycji związanych ze stadionami zrealizowana była w 50 % , na części modernizowanych lub budowanych od podstaw stadionów prace ukończone były jednak nawet w 80 % .
Miasta i stadiony .
Organizacją finałów mistrzostw świata w Brazylii zainteresowanych było 17 miast : São Paulo , Rio de Janeiro , Belo Horizonte , Porto Alegre , Brasília , Belém , Campo Grande , Cuiabá , Kurytyba , Florianópolis , Fortaleza , Goiânia , Manaus , Natal , Recife/Olinda ( stadion będzie własnością obu miast ) , Rio Branco i Salvador .
Maceió zrezygnowało w styczniu 2009 .
Zgodnie z zasadami FIFA każdy ze stadionów FIFA musi być zlokalizowany w innym mieście , a liczba miast organizujących turniej wynosi od 8 do 10 .
Mimo to 26 grudnia 2008 Brazylijczykom udało się uzyskać pozwolenie na wybranie 12 miast .
Ostateczną decyzje w sprawie wyboru miast ogłoszono 31 maja 2009 .
Belém , Campo Grande , Florianópolis , Goiânia i Rio Branco zostały odrzucone .
Mecz otwarcia odbył się w São Paulo , mecz o 3. miejsce w Brasílii , zaś finał w Rio de Janeiro .
W grupie reprezentacja gospodarzy zagrała na otwarcie w São Paulo , potem w Fortalezie , a na końcu w Brasílii .
Ponadto na każdym ze stadionów były rozgrywane co najmniej 4 mecze .
