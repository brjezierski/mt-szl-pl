Mitch Mitchell
Mitch Mitchell właściwie John Mitchell ( ur .
9 lipca 1947 , zm .
12 listopada 2008 ) – angielski perkusista rockowy i jazzowy .
Muzyk zyskał popularność jako perkusista grupy The Jimi Hendrix Experience .
W 2007 roku został sklasyfikowany na 18. miejscu listy 50 najlepszych perkusistów rockowych według Stylus Magazine .
