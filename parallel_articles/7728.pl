Mixiomycetes
Mixiomycetes R. Bauer et al .
– klasa podstawczaków ( `` Basidiomycota '' ) .
Jest to takson monotypowy , zawierający także monotypowe : rząd Mixiales , rodzinę Mixiaceae i rodzaj Mixia zawierający gatunek Mixia osmundae .
Charakterystyka .
Gatunek `` Mixia osmundae '' , jedyny w całej klasie `` Mixiomycetes '' jest pasożytem atakującym paproć `` Osmunda japonica '' z rodziny długoszowych .
Na sztucznej pożywce przyjmuje formę drożdżowatą .
