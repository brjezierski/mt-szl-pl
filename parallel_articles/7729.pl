Peristemma pseudosphaeria
Peristemma pseudosphaeria ( Mont . )
Jørst .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Miyagia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1840 r. Camille Montagne , nadając mu nazwę `` Puccinia pseudosphaeria '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Ivar Jørstad w 1956 r .
Synonimy :
Morfologia i rozwój .
Pasożyt jednodomowy , tzn .
że cały jego cykl życiowy odbywa się na jednym żywicielu .
Jest też rdzą niepełnocyklową – wytwarza tylko dwa rodzaje zarodników ; urediniospory i teliospory .
Na górnej stronie liści porażonych roślin powstają brunatne plamy , na dolnej tworzą się uredinia i telia otoczone rzędem purpurowobrązowych parafiz .
Kupki urediniów tworzą się pojedynczo lub grupowo na skórce rośliny .
