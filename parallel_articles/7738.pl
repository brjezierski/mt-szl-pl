Miłosna ( przystanek kolejowy )
Miłosna – nieistniejący już przystanek osobowy w Kwidzynie , w dzielnicy Miłosna , w powiecie kwidzyńskim , w województwie pomorskim , w Polsce .
Została otwarta w 1900 roku razem z linią z Kwidzyna do Kisielic .
Do 1945 roku linia ta była używana w ruchu pasażerskim i towarowym , w tym samym roku jej tory zostały rozebrane .
