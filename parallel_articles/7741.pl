Mionów
Mionów ( dodatkowa nazwa w j. niem .
Polnisch Müllmen ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 205 osób .
Częścią wsi są Hojnowice .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 8,5 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy gminy Głogówek z gminą Biała .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Środowisko naturalne .
W Mionowie panuje klimat umiarkowany ciepły .
