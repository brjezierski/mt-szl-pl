Miadzioł ( miasto )
Miadzioł ( biał .
Мядзел , `` Miadzieł '' , Мядзель , `` Miadziel '' , ros .
Мядель ) – miasto na Białorusi , w obwodzie mińskim , siedziba rejonu miadzielskiego .
Miasto królewskie położone było w końcu XVIII wieku w starostwie niegrodowym miadziolskim w powiecie oszmiańskim województwa wileńskiego , do 1945 miasteczka ( Stary i Nowy Miadzioł ) oraz majątek Miadzioł leżały w Polsce , w województwie wileńskim , w powiecie postawskim .
Siedzibą gminy Miadzioł był Miadzioł Nowy .
Miadzioł jest położony na Pojezierzu Święciańskim nieopodal jeziora Miastro wliczanego do grupy największego białoruskiego jeziora – Narocz .
W pobliżu miejscowości znajduje się też jezioro Miadzioł .
Historia .
Dawniej miejscowość dzieliła się na Stary Miadzioł , wzmiankowany po raz pierwszy w 1454 r. , i Nowy Miadzioł wzmiankowany w 1462 r. , z którego prowadził most do murowanego zamku wzniesionego w XVI wieku na wyspie jeziora Miastro .
W dwudziestoleciu międzywojennym Stary i Nowy Miadzioł oraz majątek Miadzioł leżały w Polsce , w województwie wileńskim , w powiecie duniłowickim ( od 1926 postawskim ) , w gminie Miadzioł .
