Lista skrótów nazwisk botaników i mykologów
Alfabetyczna lista skrótów niektórych nazwisk botaników i mykologów umieszczanych w nazwach naukowych roślin i grzybów .
Najczęściej są to autorzy , którzy w publikacjach jako pierwsi ważnie opisali dany takson i nadali mu nazwę systematyczną .
Wykaz standardowych skrótów autorów diagnoz taksonomicznych jest publikowany przez Królewskie Ogrody Botaniczne w Kew ( `` Authors of Plant Names '' ) oraz Index Fungorum .
Uwaga : W druku ( np .
w artykułach biologicznych ) często stosuje się konwencję typograficzną pisania tych nazwisk kapitalikami .
