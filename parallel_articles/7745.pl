Miastko ( stacja kolejowa )
Miastko – stacja kolejowa w Miastku , w województwie pomorskim , w Polsce , na linii kolejowej nr 405 , łączącej stację Piła Główna z Ustką .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Dawniej była to stacja węzłowa , odgałęziała się tu linia w stronę Bytowa , została ona jednak rozebrana w roku 1945 .
W roku 2017 stacja obsługiwała 100–149 pasażerów na dobę .
Zabudowania dworcowe .
Budynek główny .
Ceglany budynek z 1904 r. utrzymany w stylu neogotyckim .
Stacja wodna .
Wieża wybudowana na potrzeby kolei .
Znajduje się w pobliżu głównego budynku dworca kolejowego , po przeciwnej stronie torów , poniżej dzisiejszej ulicy Kolejowej , od której oddzielona jest murem oporowym .
