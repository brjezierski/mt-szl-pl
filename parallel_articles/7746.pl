Miastkowo
Miastkowo – wieś ( dawniej miasto ) w Polsce położona w województwie podlaskim , w powiecie łomżyńskim , w gminie Miastkowo .
Leży przy drodze krajowej nr 61 .
Leży na Mazowszu , w ziemi łomżyńskiej .
Miejscowość jest siedzibą gminy Miastkowo .
Historia .
Miastkowo wzmiankowane w 1413 r. , kiedy to książę Janusz darował Jakuczowi z Borkowa wójtostwo we wsi Zabiele .
`` Gniazdo '' rodu Miastkowskich .
Parafia wraz z kościołem erygowana przez księcia mazowieckiego Bolesława w 1451 r .
Według lustracji z 1660 r. `` miastkowskie starostwo niegrodowe '' zostało wydzielone ze `` starostwa wiźnieńskiego '' .
W 1771 r. zarządzał nim Michał Staniszewski , skarbnik łomżyński , opłacając 534 zł .
