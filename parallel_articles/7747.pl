Miastkówek
Miastkówek – wieś w Polsce położona w województwie lubelskim , w powiecie lubartowskim , w gminie Michów .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa lubelskiego .
Wieś stanowi sołectwo gminy Michów .
Według Narodowego Spisu Powszechnego z roku 2011 wieś liczyła 206 mieszkańców .
