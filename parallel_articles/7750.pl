Miastowice
Miastowice – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie nakielskim , w gminie Kcynia .
Położenie .
Wieś znajdowała się na końcu cukrowniczej linii wąskotorowej ( rozstaw szyn : 600 mm , długość linii : 46 km , budowa : 1884–1893 ) należącej do cukrowni Żnin .
Podział administracyjny .
W latach 1950–1975 miejscowość administracyjnie należała do tzw .
dużego województwa bydgoskiego , a w latach 1975–1998 do tzw .
małego województwa bydgoskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 335 mieszkańców .
Jest siódmą co do wielkości miejscowością gminy Kcynia .
