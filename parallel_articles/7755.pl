Basen miejski przy ulicy gen. Józefa Bema w Raciborzu
Basen miejski przy ul .
Bema w Raciborzu – otwarte kąpielisko miejskie , oddane do użytku w 1925 roku , użytkowane aż do powodzi w 1997 roku .
Historia .
Racibórz , jedno z pierwszych miast na Górnym Śląsku , posiadał otwarte kąpielisko , które znajdowało się nad Odrą przy ul .
1 Maja .
Połączone było z rzeką , co warunkowało możliwość korzystania z niego w zależności od poziomu wody w Odrze .
Ograniczenia w użytkowaniu kąpieliska spowodowały powstanie koncepcji wybudowania nowej pływalni .
9 września 1923 roku uchwałą rady miejskiej zdecydowano o powstaniu nowego kąpieliska .
Budowa rozpoczęła się w 1924 roku , a zakończyła rok później .
W lipcu 1925 roku dokonano oficjalnego otwarcia nowego obiektu .
Koszt budowy kąpieliska wyniósł 240 tys .
