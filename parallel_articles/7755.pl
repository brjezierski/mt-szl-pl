Kąpielisko miejskie było wykorzystywane podczas I Olimpiady Górnośląskiej , która odbyła się we wrześniu 1925 roku .
Po II wojnie światowej kąpielisko nadal służyło mieszkańcom , aż do powodzi w 1997 roku , podczas której zostało zniszczone i w związku z tym zamknięte .
