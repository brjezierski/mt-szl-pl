Siedzibą władz gminy był Mierzeszyn .
Gmina Mierzeszyn powstała po II wojnie światowej ( 1945 ) w części byłego Wolnego Miasta Gdańsk , w której utworzono powiat gdański ( ziemie te wchodziły w skład tzw .
