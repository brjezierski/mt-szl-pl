Miesiąc
Miesiąc – różnie definiowana jednostka czasu , zwykle obejmująca od 28 do 31 dób .
Miesiące kalendarzowe zawierają całkowite liczby dni , jednak miesiące astronomiczne ( księżycowe ) są związane z ruchem Księżyca wokół Ziemi i nie mają tej własności .
