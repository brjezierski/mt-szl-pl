Astronomiczna geneza pojęcia odbiła się w dawnej polskiej nazwie , która była synonimem słowa Księżyc ( `` księżyc '' dosłownie `` syn księcia '' , tj .
Słońca ) .
W miarę integracji kalendarzowych systemów solarnych i lunarnych pierwotna długość miesiąca przestała być powiązana z fizycznymi zmianami Księżyca .
Numery miesięcy zapisuje się zwyczajowo w języku polskim cyframi rzymskimi , choć w związku z rozwojem informatyki przewagę zyskuje zapis cyframi arabskimi .
