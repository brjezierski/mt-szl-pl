Miodusy-Stasiowięta
Miodusy-Stasiowięta – wieś w Polsce położona w województwie podlaskim , w powiecie wysokomazowieckim , w gminie Wysokie Mazowieckie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa łomżyńskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Michała Archanioła w Jabłonce Kościelnej .
Historia .
Dawniej : `` Miodusy '' albo `` Miodusze '' .
W roku 1827 miejscowość liczyła 15 domów i 89 mieszkańców .
W końcu wieku XIX wieś drobnoszlachecka w powiecie `` mazowieckim '' , gmina i parafia Jabłonka .
W pobliżu inne wsie tworzące tzw .
