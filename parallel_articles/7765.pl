Miecław
Miecław ( Masław , Mojsław , Miesław ; zm .
1047 ) – cześnik króla Mieszka II , następnie władca Mazowsza w czasie wygnania króla , kontestujący władzę Piastów .
Działalność polityczna .
Po śmierci króla Mieszka II i ucieczce z kraju na Węgry jego syna , księcia Kazimierza Odnowiciela w kraju zapanował chaos oraz wybuchło w 1038 powstanie ludowe .
W tym czasie Miecław objął władzę na Mazowszu ogłaszając się księciem ( 1037 ) .
Założył tzw .
„ państwo Miecława ” .
Według ustaleń Mateusza Boguckiego Miecław umocnił swoje panowanie przez emisję własnych monet .
Gdy Kazimierz powrócił w 1039 roku do Polski , Miecław przeciwstawił się mu zbrojnie w 1041 roku , sprzymierzając się z Pomorzanami i Jaćwingami .
Konflikt ten znany jest w historiografii polskiej jako „ bunt Miecława ” .
Kazimierz odniósł jednak zwycięstwo w bitwie pod Pobiedziskami na terenie Wielkopolski .
Upadła wtedy koncepcja ustanowienia przez Miecława nowej dynastii .
Według Galla Anonima Miecław zginął w 1047 roku w bitwie z wojskami księcia Kazimierza Odnowiciela i posiłkami księcia kijowskiego Jarosława Mądrego .
Według przekazu Wincentego Kadłubka Miecław miał zbiec do Prus i dopiero tam zostać zamordowanym .
Geneza imienia .
