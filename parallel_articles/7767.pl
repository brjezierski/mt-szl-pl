Demografia .
W czasie swojego istnienia , NRD zamieszkana była przez około 17 milionów osób , w latach 1949–1990 liczba ludności spadła o ok. 10 % .
Ustrój .
NRD było państwem o ustroju socjalistycznym , wprowadzono system polityczny i społeczny oparty na wzorach radzieckich .
Najwyższym organem władzy państwowej NRD była Izba Ludowa ( niem .
`` Volkskammer '' ) , która wybierała Radę Państwa ( niem .
`` Staatsrat der DDR '' ) .
Władzę wykonawczą sprawował rząd odpowiedzialny przed Izbą Ludową .
Kierowniczą rolę sprawowała Niemiecka Socjalistyczna Partia Jedności ( `` SED '' ) , a prócz niej istniały jeszcze cztery inne partie polityczne .
Wszystkie partie i organizacje masowe działały we wspólnym bloku – Narodowy Front Niemieckiej Republiki Demokratycznej ( niem .
`` Nationale Front '' ) .
