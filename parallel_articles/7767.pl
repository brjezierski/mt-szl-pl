Niemiecka Republika Demokratyczna
Niemiecka Republika Demokratyczna ( oficjalny skrót NRD ; niem .
`` Deutsche Demokratische Republik '' , `` DDR '' , potocznie Niemcy Wschodnie ) – istniejące w Europie środkowej w latach 1949–1990 państwo niemieckie powstałe 7 października 1949 roku na terenie radzieckiej strefy okupacyjnej ( niem .
`` Sowjetische Besatzungszone in Deutschland , SBZ '' ) będące częścią bloku wschodniego .
Likwidacja NRD nastąpiła 3 października 1990 roku , gdy powstałe w miejsce NRD landy przystąpiły do RFN tworząc zjednoczone Niemcy .
Geografia .
NRD graniczyła od zachodu z Republiką Federalną Niemiec , od południa z Czechosłowacką Republiką Socjalistyczną , od wschodu z Polską Rzeczpospolitą Ludową oraz z enklawą Berlin Zachodni wewnątrz terytorium NRD , a jej całkowita powierzchnia liczyła 108 333 km² .
Północną część NRD stanowiło pobrzeże Morza Bałtyckiego i obszar Pojezierzy Meklemburskich .
Północną i środkową stanowiła Nizina Północnoniemiecka oraz Nizina Sasko-Łużycka , na południu rozciągał się obszar wyżyn Średniogórza Niemieckiego oraz gór Masywu Czeskiego .
