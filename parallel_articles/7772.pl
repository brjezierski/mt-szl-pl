Międzynarodowy alfabet fonetyczny
Międzynarodowy alfabet fonetyczny ( skrót MAF ) – system alfabetyczny transkrypcji fonetycznej oparty w większości na alfabecie łacińskim .
Został opracowany pod koniec XIX wieku przez Międzynarodowe Towarzystwo Fonetyczne jako ustandaryzowana reprezentacja dźwięków we wszystkich językach mówionych .
MAF używany jest w leksykografii , przez nauczycieli i studentów języka obcego , językoznawców , logopedów , piosenkarzy , aktorów , twórców języków sztucznych oraz tłumaczy
Jest on szeroko stosowanym środkiem standaryzacji zapisu fonetycznego dla wszystkich języków świata .
Większość znaków została zaczerpnięta wprost z alfabetu łacińskiego lub stworzona na jego podstawie , część pochodzi z alfabetu greckiego , a część została stworzona specjalnie na jego potrzeby .
MAF jest wynikiem wieloletniej pracy fonetyków zrzeszonych w Międzynarodowym Towarzystwie Fonetycznym , założonym w Paryżu w 1886 roku .
Zarówno Towarzystwo , jak i stworzony przez nie alfabet znane są pod nazwą IPA ( jest to skrótowiec od `` International Phonetic Association '' ) .
W ciągu swego istnienia alfabet IPA był kilkakrotnie zmieniany .
Znaczne modyfikacje wprowadzono w 1989 roku , po konferencji w Kilonii .
Następne zmiany przyniósł rok 1993 .
Najnowsza wersja alfabetu została opublikowana w roku 2005 .
Międzynarodowe Towarzystwo Fonetyczne zaleca , by transkrypcję fonetyczną umieszczać w nawiasach kwadratowych : „ [ ] ” .
Z kolei transkrypcja fonemiczna powinna być umieszczana między ukośnikami : „ / / ” .
