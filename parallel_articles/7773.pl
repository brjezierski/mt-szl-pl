Obchody święta w Polsce .
W 2012 roku w Polsce wystartowała kampania społeczna `` Ojczysty – dodaj do ulubionych '' zorganizowana przez Narodowe Centrum Kultury i Radę Języka Polskiego .
Ma ona na celu podnoszenie świadomości językowej oraz kształtowanie poczucia odpowiedzialności za polszczyznę .
21 lutego 2013 ruszyła nowa kampania społeczna : `` Język polski jest ą-ę '' .
Jej celem jest obrona znaków diakrytycznych i przeciwdziałanie nieużywaniu charakterystycznych polskich liter .
