Miąskowo ( powiat leszczyński )
Miąskowo – wieś w Polsce położona w województwie wielkopolskim , w powiecie leszczyńskim , w gminie Osieczna .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako Miaskowo należała do wsi mniejszych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Miaskowo należało do okręgu krzywińskiego tego powiatu i stanowiło siedzibę majątku , którego właścicielem był wówczas ( 1846 ) Zakrzewski .
Według spisu urzędowego z 1837 roku Miaskowo liczyło 84 mieszkańców , którzy zamieszkiwali 10 dymów ( domostw ) .
W latach 1975–1998 miejscowość należała administracyjnie do województwa leszczyńskiego .
