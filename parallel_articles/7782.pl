Mladá Boleslav
Mladá Boleslav ( ) – miasto statutarne w Czechach , w kraju środkowoczeskim o obszarze 28,89 km² .
Leży na lewym brzegu rzeki Izera .
Mladá Boleslav jest siedzibą powiatu Mladá Boleslav .
Od 1 stycznia 2003 powiaty nie są już jednostką podziału administracyjnego Czech , jednak podział na powiaty zachowały sądy , policja i niektóre inne urzędy państwowe .
Został również zachowany dla celów statystycznych .
Mladá Boleslav jest siedzibą koncernu Škoda Auto oraz zespołu Škoda WRC .
