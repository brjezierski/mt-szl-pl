Mo i Rana
Mo i Rana – miasto w Norwegii , liczące 18 555 mieszkańców ( 2016 ) .
Położone w gminie Rana w okręgu Nordland .
Ośrodek przemysłu spożywczego i hutniczego .
Trzecie co do wielkości miasto w północnej Norwegii .
Nazwa miasta .
W wolnym tłumaczeniu nazwa miasta oznacza „ Mo w ( gminie ) Rana ” .
Sami mieszkańcy mówią na swoje miasto Mo , a dodatek „ i Rana ” jest stosowany , by odróżnić miasto od innych miejscowości o nazwie Mo .
Biblioteka Narodowa .
W mieście znajduje się druga siedziba Norweskiej Biblioteki Narodowej .
W magazynach są przechowywane egzemplarze obowiązkowe oraz jest prowadzona digitalizacja zbiorów .
