Telefon komórkowy
Telefon komórkowy , pot .
komórka – telefon działający w oparciu o telefonię komórkową .
Obecnie absolutna większość telefonów komórkowych działa w systemach cyfrowych ( np .
GSM , DCS , PCS , UMTS ) .
W starszych rozwiązaniach stosowano sieci analogowe ( NMT , AMPS ) .
Historia .
Próby stworzenia telefonów komórkowych miały miejsce już w latach 40. i 50 .
W 1946 w USA zbudowano sieć i rozpoczęto eksploatację mobilnych telefonów radiowych Motorola dla klientów prywatnych w samochodach osobowych co było poprzednikiem telefonii komórkowej .
Był to jedyny kraj świata gdzie system został zaakceptowany i zdobył popularność wśród cywilów .
Amerykańska policja używa komunikacji bezprzewodowej od 1928 .
Pierwszy prototyp telefonu komórkowego wykonała szwedzka firma Ericsson w 1956 .
Telefon ważył 40 kilogramów , a kształtem przypominał walizkę .
Urządzenie to kosztowało tyle co samochód .
3 kwietnia 1973 w Nowym Jorku firma Motorola wprowadziła do obrotu swój pierwszy telefon komórkowy DynaTAC ( `` DYNamic Adaptive Total Area Coverage '' ) .
Telefon ważył 0,8 kg i miał wymiary cegły .
16 września 1975 opublikowano amerykańskie zgłoszenie patentowe US 3,906,166 firmy Motorola pod tytułem `` Radio telephone system '' .
Pierwsza sieć telefonii komórkowej została uruchomiona w Japonii w 1979 ( NTT ) , natomiast w Polsce w 1992 ( Centertel ) .
21 sierpnia 1983 Federalna Komisja Łączności dopuściła na rynek DynaTAC 8000X .
3 grudnia 1992 roku wysłano po raz pierwszy wiadomość SMS .
Według danych statystycznych , pod koniec 2018 roku w Polsce było 48 286 000 abonentów telefonii komórkowej .
