Modliszewo
Modliszewo – wieś w Polsce położona w województwie wielkopolskim , w powiecie gnieźnieńskim , w gminie Gniezno .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
Historia .
Pierwsza wzmianka o Modliszewie , zwanym wtedy Modliszewem Wielkim ( `` Modlschewo Wyelkye '' ) , znajduje się w spisie dóbr z 1247 r. w związku z posadowieniem drugiego dworu na polu zwanym Łabiszynek .
Nazwa Modliszewo pochodzi od Modlisza , bliżej nieznanego miejscowego władcy , obejmuje dobra szlacheckie Dryjów-Sobiejuchów – później – Modliszewskich-Sitków .
