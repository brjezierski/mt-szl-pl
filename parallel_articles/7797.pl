Modrzew ( Łódź )
Modrzew ( dawn .
`` Modrzewie '' ) – dawna podłódzka miejscowość , od 1946 osiedle w zachodniej części Łodzi , w dzielnicy Bałuty .
Administracyjnie wchodzi w skład osiedla Wzniesień Łódzkich .
Jest to ulicówka rozpościerająca się wzdłuż ulicy Żółwiowej i – w przedłużeniu – ulicy Okólnej .
Modrzew ma skrajnie peryferyjne położenie na samej północy Łodzi .
Pod Modrzewiem rozpoczyna bieg Bzura Łagiewniczanka .
Historia .
Modrzew to dawna wieś , od 1867 w gminie Łagiewniki w powiecie łódzkim .
