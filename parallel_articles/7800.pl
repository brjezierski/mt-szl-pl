Mogadiszu
Mogadiszu ( ; ) – stolica i największe miasto w Somalii , nad Oceanem Indyjskim , także port morski i centrum handlowe .
W 2017 roku miasto liczyło 2 590 000 ludności .
Miasto zostało założone w IX wieku przez Arabów jako Makdaszu .
W średniowieczu miasto to pełniło funkcje ośrodka handlowego , utrzymywało kontakty handlowe m.in .
z Portugalią i imamem Maskatu .
W 1871 r. miasto zostało zajęte przez sułtana Zanzibaru , który w 1892 r. wydzierżawił port Włochom .
Od 1960 r. stolica niepodległej Somalii .
Od 1991 r. wojna domowa doprowadziła do dewastacji i destabilizacji stolicy ( kontrolowanej przez pięć zbrojnych klanów ) , w 1993 roku amerykańskie wojska stoczyły bitwę znaną jako bitwa o Mogadiszu , na kanwie tej historii Ridley Scott nakręcił film `` Helikopter w ogniu '' .
Operacja wojskowa pod egidą ONZ nazywała się „ Restore Hope ” co oznacza „ Przywrócić nadzieję ” .
W operacji tej brała również udział francuska Legia Cudzoziemska .
W czerwcu 2006 r. zdobyte przez islamskich fundamentalistów .
Pod koniec grudnia 2006 r. przejęte jednak przez wojska rządowe .
Jest najważniejszym ośrodkiem polityczno-gospodarczym kraju .
Znajdują się tu zakłady przemysłu spożywczego ( przetwórstwo ryb , produkty mięsne ) włókienniczego i chemicznego .
Liczne zabytki – meczety z XIII i XIX wieku .
Geografia .
Mogadiszu jest położone na wybrzeżu Morza Somalijskiego , w Rogu Afryki , w regionie Banaadir , w południowo-wschodniej Somalii .
Miasto jest podzielone na 16 dystryktów .
Bezpieczeństwo .
Regularnie znajduje się w czołówce najbardziej niebezpiecznych miast w Afryce .
Według danych z 2021 roku współczynnik zabójstw wynosi 73,8 na 100 tys .
mieszkańców .
