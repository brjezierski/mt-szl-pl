Mogi das Cruzes
Mogi das Cruzes − miasto w Brazylii , w stanie São Paulo .
Region metropolitalny w São Paulo .
Liczba mieszkańców wynosi 387 241 .
Urodził się tu brazylijski piłkarz Neymar .
Historia .
Miasto zostało założone przez Bandeirantes w 1560 roku i służył jako punkt odpoczynku dla
pionierów którzy odkrywali dalsze tereny w głąb Brazylii .
