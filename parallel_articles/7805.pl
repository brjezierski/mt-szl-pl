Mogilno ( jezioro )
Mogilno ( niem .
`` Moglin See '' ) – jezioro rynnowe w Polsce położone w województwie zachodniopomorskim , w powiecie choszczeńskim , w gminie Pełczyce .
Akwen leży w kompleksie leśnym na Pojezierzu Choszczeńskim , około 1 km na południe od jeziora Pełcz .
Od strony zachodniej silnie porastające roślinnością wodną , powierzchnia tafli wody w 2019 roku wynosiła około 7 ha .
