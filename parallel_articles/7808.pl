Marinduque
Marinduque – wyspa i prowincja na Filipinach w regionie MIMAROPA .
Jest położona pomiędzy zatoką Tayabas na północy i morzem Sibuyan na południu .
Na zachód od południowo-zachodniego wybrzeża Luzon ( prowincja Quezon ) , na wschodzie od wyspy Mindoro i na północ od wyspy Romblon .
Jest to wulkaniczna wyspa o górzystej powierzchni z najwyższym szczytem 1157 m n.p.m .
Klimat podzwrotnikowy , wilgotny .
