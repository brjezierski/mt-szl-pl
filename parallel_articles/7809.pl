Maj
Maj jest miesiącem wiosennym na półkuli północnej , a na południowej jesiennym .
W ciągu trwania miesiąca maja w Polsce dzień wydłuża się do blisko 16,5 godziny .
Nazwa .
Nazwa miesiąca pochodzi od łacińskiej nazwy `` Maius '' ( zobacz : kalendarz rzymski ) .
Miesiąc ten ma nazwę bogini Mai , matki boga Merkurego .
Została ona zapożyczona przez większość języków europejskich .
W języku polskim określenie `` maić '' oznacza tyle samo , co stroić .
Staropolska nazwa maja brzmiała `` trawień '' .
