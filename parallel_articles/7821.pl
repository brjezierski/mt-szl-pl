Molde
Molde – norweskie miasto i gmina leżąca w gminie Møre og Romsdal , w okręgu Romsdal .
Położone jest na półwyspie Romsdalshalvøya , nad wodami Romsdalsfjordu .
Jest centrum administracyjnym okręgu Møre og Romsdal , gminy Molde , głównym miastem regionu Romsdal oraz siedzibą diecezji Møre .
Gmina ma powierzchnię 362,02 km² , co czyni ją 254. norweską gminą pod tym względem .
Zamieszkana jest przez 24 795 osób ( 2010 ) .
