Mona Lisa ( film )
Mona Lisa – brytyjski film kryminalny z 1986 roku w reżyserii Neila Jordana .
Zdjęcia kręcono w Londynie i w Brighton .
Fabuła .
Były skazaniec George ( po siedmiu latach więzienia ) zostaje kierowcą i ochroniarzem luksusowej prostytutki Simone w Londynie .
Simone prosi go o odszukanie przyjaciółki , co wywołuje kłopoty George ' a .
