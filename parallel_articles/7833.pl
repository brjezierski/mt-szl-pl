Monczegorsk
Monczegorsk ( ros .
Мончегорск ) – miasto w północno-zachodniej Rosji w obwodzie murmańskim .
Położone na Półwyspie Kolskim , w Kole podbiegunowym północnym , nad jeziorem Imandra , 145 km na południe od Murmańska .
W 2005 liczyło 50700 mieszkańców .
Powstało w 1937 jako centrum przemysłu wydobywczo-przetwórczego rud miedzi i niklu dzięki ocenie naukowca Aleksandra Fersmana , który przywidywał występowanie w tym rejonie bardzo bogatych złóż rudy niklowo-miedzianej .
Złoża okazały się jednak mniej bogate i ruda do przerobu przywożona jest w głównej mierze z Norylska .
W okolicy występują rzadkie minerały np .
Monczetundrait .
W mieście znajduje się dyrekcja Lapońskiego Rezerwatu Biosfery .
