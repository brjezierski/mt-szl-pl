Mondsee ( jezioro w Austrii )
Mondsee – jezioro w Austrii w regionie Salzkammergut .
Administracyjnie znajduje się w kraju związkowym Górna Austria .
Zajmuje powierzchnię 13,78 km² .
Średnia głębokość wynosi 37 m , zaś maksymalna 68 m. Lustro wody znajduje się na wysokości 481 m n.p.m .
Na północno-zachodnim brzegu położone jest miasto Mondsee , zaś po przeciwnej stronie wieś Oberburgau .
W tym ostatnim z jeziora wypływa krótka rzeka Seeache , łącząca je z jeziorem Attersee .
Rzekami zasilającymi Mondsee są Zeller Ache , Fuschler Ache i Wangauer Ache .
