Monika Rosa
Monika Anna Rosa ( ur .
24 kwietnia 1986 w Czeladzi ) – polska polityk i politolog , posłanka na Sejm VIII i IX kadencji .
Życiorys .
Z pochodzenia Ślązaczka .
Wychowywała się w Gliwicach , skąd pochodzą jej rodzice .
Ukończyła politologię na Wydziale Nauk Społecznych Uniwersytetu Śląskiego w Katowicach .
Pracowała w Radiu CCM , a w latach 2008–2010 zajmowała stanowisko koordynatorki w dziale HR Opteam Poland w Katowicach .
Od 2010 była urzędniczką w Kancelarii Prezydenta RP w okresie prezydentury Bronisława Komorowskiego , pełniła m.in .
funkcję zastępcy dyrektora Zespołu Obsługi Bieżącej w Gabinecie Prezydenta RP .
Zajmowała się m.in .
koordynowaniem obchodów 25. rocznicy wyborów z 1989 .
Współtworzyła czasopismo „ Liberté ! ” , weszła w skład jego redakcji .
Działała w Młodym Centrum , następnie w .
W wyborach parlamentarnych w 2015 kandydowała do Sejmu w okręgu katowickim z pierwszego miejsca na liście Nowoczesnej Ryszarda Petru .
Uzyskała mandat posłanki VIII kadencji , otrzymując 20 126 głosów .
W wyborach w 2019 z powodzeniem ubiegała się o poselską reelekcję z ramienia Koalicji Obywatelskiej , otrzymując 13 918 głosów .
W Sejmie IX kadencji została członkinią m.in .
Komisji Mniejszości Narodowych i Etnicznych , Komisji Polityki Społecznej i Rodziny oraz Komisji Zdrowia .
