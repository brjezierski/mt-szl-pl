Monilinia polystroma
Monilinia polystroma ( G. Leeuwen ) L.M .
Kohn – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Jest jednym z patogenów powodujących brunatną zgniliznę drzew pestkowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Monilinia , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 2002 r. G. Leeuwen , nadając mu nazwę `` Monilia polystroma '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu L.M .
Kohn w 2014 r .
Historia odkrycia gatunku .
Badania van Leeuwena i innych w 2002 r. przeprowadzone na izolatach `` Monilinia fructigena '' pobranych z jabłek jabłoni niskiej ( `` Malus pumila '' ) wykazały , że istniały znaczące różnice między izolatami europejskimi i japońskimi pod względem charakterystyki kolonii , tworzenia podkładek , tempa wzrostu i wymiarów konidiów .
Różnice między dwiema grupami zostały wcześniej wykazane przy użyciu regionu ITS rybosomalnego DNA .
