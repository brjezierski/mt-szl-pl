Monilinia fructicola
Monilinia fructicola ( G. Winter ) Honey – gatunek grzybów z rodziny twardnicowatych ( `` Sclerotiniaceae '' ) .
Jest jednym z patogenów powodujących brunatną zgniliznę drzew pestkowych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Monilinia , Sclerotiniaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1883 r. Heinrich Georg Winter , nadając mu nazwę `` Ciboria fructicola '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Edwin Earle Honey w 1928 r .
Synonimy :
Morfologia .
Strzępki pierwotne o średnicy 7–10 μm , długości nawet powyżej 250 μm , cienkościenne .
Tworzą jedno lub kilka odgałęzień przed pierwszą przegrodą .
Odgałęzienia zwykle są węższe .
