Monochaetinula ampelophila
Monochaetinula ampelophila ( Speg . )
Nag Raj – gatunek grzybów z klasy `` Sordariomycetes '' .
Pasożytniczy grzyb mikroskopijny wywołujący u rokitnika zwyczajnego chorobę o nazwie kolista nekroza pędów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Amphisphaeriaceae , Amphisphaeriales , Xylariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Zdiagnozował go w 1910 r. Carlo Luigi Spegazzini nadając mu nazwę `` Monochaetia ampelophila `` .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Tumkur R. Nag Raj w 1993 r .
