Monoicomyces nigrescens
Monoicomyces nigrescens Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Monoicomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał Roland Thaxter w 1902 r. na odwłoku owadów z rodzajów `` Calodera '' i `` Tachyusa '' w USA .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
W Polsce Tomasz Majewski w 1994 i 2003 r. opisał jego występowanie na chrząszczach z rodziny kusakowatych ( Staphylinidae ) : `` Atheta celata '' , `` Atheta nigra '' , `` Atheta parvula '' , `` Atheta sardidula '' i `` falagria nigra '' .
