Monoicomyces similis
Monoicomyces similis Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Pasożyt owadów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Monoicomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1905 r. Roland Thaxter na owadach z rodzajów `` Hamalota '' i `` Lactarius '' .
Charakterystyka .
Grzyb entomopatogeniczny , pasożyt zewnętrzny owadów .
Nie powoduje ich śmierci i wyrządza im niewielkie szkody .
W Polsce Tomasz Majewski opisał jego występowanie na chrząszczu `` Dinaraea aequata '' z rodziny biegaczowatych ( Carabidae ) .
