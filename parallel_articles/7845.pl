Monopoli ( stacja kolejowa )
Monopoli ( wł : `` Stazione di Monopoli '' ) – stacja kolejowa w Monopoli , w prowincji Bari , w regionie Apulia , we Włoszech .
Położona jesta pomiędzy stacjami Barii i Brindisi , na linii kolejowej Adriatica .
Stacja znajduje się w pobliżu centrum miasta , w zachodniej części Monopoli .
