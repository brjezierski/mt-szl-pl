Monostichella robergei
Monostichella robergei ( Desm . )
Höhn . )
– gatunek grzybów z typu workowców ( `` Ascomycota '' ) .
Grzyb mikroskopijny , pasożyt , wywołujący plamistość liści u grabu pospolitego ( `` Carpinus betulus '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Monostichella , Incertae sedis , Incertae sedis , Incertae sedis , Incertae sedis , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1853 r. Jean Baptiste Henri Joseph Desmazières nadając mu nazwę `` Gloeosporium robergei '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Franz von Höhnel w 1916 r .
Synonimy :
Objawy i szkodliwość .
Patogen rozwija się tylko na liściach grabu pospolitego , powodując powstawanie na nich brązowych plam .
