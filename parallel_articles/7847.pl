Katedra .
Katedra w Monreale jest największą budowlą z okresu panowania Normanów na północnej Sycylii .
Jej budowę rozpoczął w 1174 Wilhelm II Dobry , w celu ograniczenia rosnących wpływów biskupstwa Palermo , a w szczególności tamtejszego biskupa Gualtierro Offamilio .
Dlatego też wraz z kościołem wzniesiono pałac arcybiskupi i budynki klasztoru benedyktynów .
Otoczono je potężnymi murami z dwunastoma wieżami .
