Czernidłaczek piaskowy
Czernidłaczek piaskowy , montagniówka piaskowa ( `` Montagnea arenaria '' ( DC . )
Zeller ) – gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Melanophyllum , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1815 r. Augustin Pyramus de Candolle nadając mu nazwę `` Agaricus arenarius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu S.M .
Zeller w 1943 r .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r. błędnie synonimizując go z `` Montagnea radiosa '' ( według Index Fungorum jest to odrębny takson ) .
Wanda Rudnicka-Jezierska w 1991 r. opisywała ten gatunek pod nazwą montagniówka piaskowa `` Montagnea arenaria '' .
