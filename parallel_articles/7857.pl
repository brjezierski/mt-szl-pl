Montauban
Montauban ( oksyt .
`` Montalban '' ) – miejscowość i gmina we Francji , w regionie Oksytania , w departamencie Tarn i Garonna .
W 2013 roku jej populacja wynosiła 59 630 mieszkańców .
Przez gminę przepływają rzeki Tarn oraz Aveyron .
3 listopada 1940 w Montauban zmarł Manuel Azaña , hiszpański pisarz i ostatni prezydent Drugiej Republiki Hiszpańskiej .
W Montauban urodziła się Amandine Hesse , francuska tenisistka .
