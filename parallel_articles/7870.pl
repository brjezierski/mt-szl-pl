Montecatini Terme
Montecatini Terme – miejscowość i gmina we Włoszech , w regionie Toskania , w prowincji Pistoia .
Miasteczko ma charakter sanatoryjno-letniskowy , co związane jest z tutejszymi źródłami termalnymi .
Źródła te w 1918 r. badała pod kątem radioaktywności Maria Skłodowska-Curie .
Według danych na rok 2004 gminę zamieszkiwały 20 024 osoby , 1177,9 os./km² .
