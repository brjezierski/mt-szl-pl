Montego Bay
Montego Bay – miasto w północno-zachodniej części Jamajki , nad Morzem Karaibskim .
Około 80 tys .
mieszkańców ; największy ośrodek turystyczny kraju ; kąpielisko morskie ; przemysł środków transportu , spożywczy ; port morski .
Czwarte co do wielkości miasto kraju .
Montego Bay było miejscem zakończenia `` III Konferencji Prawa Morza Organizacji Narodów Zjednoczonych '' oraz podpisania `` Konwencji Narodów Zjednoczonych o prawie morza '' .
W miejscowości mieszkał polski malarz marynista Michał Leszczyński .
