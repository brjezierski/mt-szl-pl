Montevideo ( departament )
Montevideo – urugwajski departament położony w południowej części kraju ,
nad Atlantykiem .
Graniczy z San José oraz Canelones .
Prawie połowę obszaru tego powstałego w 1816 r. departamentu zajmuje miasto Montevideo - stolica i największa metropolia Urugwaju .
Powierzchnia Montevideo wynosi 530 km² , stawiając go pod tym względem na ostatnim miejscu w kraju .
W 2004 r. departament , a tym samym miasto zamieszkiwało 1 326 064 osby ( pierwsze miejsce w kraju ) .
Dawało to rekordową w Urugwaju gęstość zaludnienia 2 502 mieszk./km² .
