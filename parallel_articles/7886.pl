Montluçon
Montluçon – miejscowość i gmina we Francji , w regionie Owernia-Rodan-Alpy , w departamencie Allier .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 44 248 osób , a gęstość zaludnienia wynosiła 2141 osób/km² .
W styczniu 2015 r. Montluçon zamieszkiwały 39 353 osoby , przy gęstości zaludnienia wynoszącej 1903,9 osób/km² .
