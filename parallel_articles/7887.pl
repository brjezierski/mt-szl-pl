Montmagny ( Kanada )
Montmagny – miasto w Kanadzie , w prowincji Quebec , w regionie Chaudière-Appalaches i MRC Montmagny .
Nazwa miasta pochodzi od Charles ' a Jacques ' a Huault de Montmagny , pierwszego seniora senioratu Rivière-du-Sud .
Terytorium zostało mu przyznane 5 maja 1646 roku .
Dzisiejsze miasto zostało później wyodrębnione z tego senioratu , który był jednym z najstarszych w regionie historycznym Côte-du-Sud .
Liczba mieszkańców Montmagny wynosi 11 353 .
Język francuski jest językiem ojczystym dla 98,9 % , angielski dla 0,3 % mieszkańców ( 2006 ) .
