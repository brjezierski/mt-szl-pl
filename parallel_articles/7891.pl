Montpellier
Montpellier ( oksyt .
`` Montpelhièr '' ) – miasto i gmina na południu Francji w odległości 10 km od wybrzeża Morza Śródziemnego , nad rzeką Lez , w regionie Oksytania , w departamencie Hérault .
Położenie geograficzne : 43°37 N , 3°52 E
Historia .
Montpellier powstało w 737 r. po połączeniu dwóch wiosek i nosiło wówczas nazwę Mons pessulanus albo Pessulus .
