Montreal
Montreal ( fr .
`` Montréal '' [ ] ) – miasto w Kanadzie , nad Rzeką Świętego Wawrzyńca w prowincji Quebec .
W latach 1844–1849 Montreal był stolicą Prowincji Kanady .
Jest to drugie co do wielkości miasto Kanady , największy port morski wschodniej części kraju oraz główny ośrodek gospodarczy i kulturalny francuskojęzycznej części Kanady .
Powstał jako niewielka osada francuska na cześć królewską nazwany `` Mont Royal '' – „ Wzgórze królewskie ” – stąd obecna nazwa .
Po przyłączeniu 1 stycznia 2002 dzielnic podmiejskich liczba mieszkańców wynosi 1,6 mln ( zespół miejski 3,5 mln , 2001 ) .
Miasto jest podzielone na 25 dzielnic ( fr .
`` arrondissements '' ) .
Niektóre z nich decyzją rządu prowincji Quebec w 2001 roku zostały przyłączone do Montrealu .
Wywołało to sprzeciw mieszkańców przyłączonych dzielnic .
W następstwie referendum z czerwca 2004 roku , 1 stycznia 2006 roku część z nowo przyłączonych dzielnic , zamieszkiwanych przez 13 % dotychczasowej populacji miasta , odłączyło się od Montrealu .
Położenie .
Zespół miejski jest położony przede wszystkim na dwóch wyspach , Île de Montréal i Jésus , leżących tuż na wschód od zbiegu Rzeki Św .
Wawrzyńca i rzeki Ottawa .
Wyspy opływa główny nurt Rzeki Św .
Wawrzyńca i jej dwie odnogi , rzeki : Rivière des Mille Îles i Rivière des Prairies .
Historia .
Panowanie francuskie .
Pierwszymi Europejczykami , którzy dotarli do wyspy , na której dzisiaj leży Montreal , byli Jacques Cartier i jego towarzysze .
Podczas swojej drugiej ekspedycji do Ameryki Północnej Cartier wyruszył z okolic dzisiejszego miasta Québecu w górę Rzeki Świętego Wawrzyńca .
