Monza ( stacja kolejowa )
Monza ( ) – stacja kolejowa w Monzy , w regionie Lombardia , we Włoszech .
Znajdują się tu 4 perony .
Historia .
Stacja została otwarta w 1840 roku jako stacja końcowa linii z Mediolanu , która była pierwszą linią kolejową zbudowaną w Lombardii , a drugą we Włoszech , po Neapol-Portici .
Pierwszy budynek został zastąpiony w 1884 .
Zburzony w 1901 roku na rzecz budowy mostu na Via Turati .
