Monêtier-Allemont
Monêtier-Allemont – miejscowość i gmina we Francji , w regionie Prowansja-Alpy-Lazurowe Wybrzeże , w departamencie Alpy Wysokie .
Demografia .
Według danych na rok 1990 gminę zamieszkiwało 228 osób , a gęstość zaludnienia wynosiła 32 osoby/km² .
W styczniu 2015 r. Monêtier-Allemont zamieszkiwało 318 osób , przy gęstości zaludnienia wynoszącej 44,5 osób/km² .
