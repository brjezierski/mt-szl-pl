Moose Jaw ( Saskatchewan )
Moose Jaw – miasto w Kanadzie w południowo-środkowej części prowincji Saskatchewan , nad rzeką Moose Jaw .
Ze względu na liczne parki i inne atrakcje , takie jak Casino Moose Jaw , miasto jest popularnym w prowincji ośrodkiem turystycznym .
Liczba mieszkańców Moose Jaw wynosi 32 132 .
Język angielski jest językiem ojczystym dla 92,5 % , francuski dla 1,4 % mieszkańców ( 2006 ) .
W mieście rozwinął się przemysł młynarski , mięsny , włókienniczy oraz rafineryjny .
