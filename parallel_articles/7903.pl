Mopti
Mopti – miasto w środkowym Mali , ośrodek administracyjny regionu Mopti , u ujścia rzeki Bani do Nigru , na 3 wyspach połączonych groblami .
Największy port na Nigrze .
Według szacunku z roku 2005 miasto liczy 108 700 mieszkańców .
Ważny ośrodek handlowy regionu rolniczego – uprawy ryżu , bawełny oraz rybołówstwo .
Lotnisko międzynarodowe `` Mopti Airport '' ( MZI ) .
Miasto zwane jest `` Wenecją Mali '' .
Atrakcje .
Mopti jest turystyczną stolicą Mali oraz wygodnym punktem startowym do największych atrakcji kraju – Timbuktu , Dżenne i Kraju Dogonów .
