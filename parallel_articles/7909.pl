Morawskie enklawy na Śląsku ( cz .
Moravské enklávy ve Slezsku ) – określenie eksklaw ( a także cypla ) Margrabstwa Morawskiego , które od 1783 do 1928 roku zarządzane były przez urzędy śląskie , lecz według morawskiego prawodawstwa .
Obecnie znajdują się one w granicach kraju morawsko-śląskiego w Czechach oraz województwa opolskiego w Polsce .
Enklawy te stanowiły część Moraw od momentu powstania Margrabstwa Morawskiego w 1182 roku .
Stan ten nie zmienił się pomimo powstania księstwa opawskiego w drugiej połowie XIII wieku , ponieważ stanowiąc majątek biskupstwa ołomunieckiego nie były podległe władcom tego księstwa .
Po I wojnie śląskiej w granicach Królestwa Prus znalazła się jedna z tych enklaw - klucz kietrzański .
