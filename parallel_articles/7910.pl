Język morawski
Język morawski ( ) – odmiana języka czeskiego używana na Morawach , niekiedy uznawana za samodzielny język .
Argumentem przeciw uznaniu osobnego języka morawskiego może być okoliczność , że na Morawach w użyciu funkcjonuje wiele zróżnicowanych gwar , znacznie bardziej od siebie odległych niż mowa z terytorium krainy czeskiej .
Coraz częściej podejmowane są jednak próby kodyfikacji narzeczy morawskich i stworzenia z nich pewnego standardu .
Działaniami tymi zajmuje się między innymi Instytut Języka Morawskiego ( ) , powołany do życia w 2006 .
Za literackie uważane są niektóre wyrazy morawskie ( podobnie jak w języku niemieckim wyrazy austriackie ) , które są na Morawach stosowane powszechnie zarówno w rozmowach , jak i w formie piśmiennej .
Inne uważane są za regionalne wyrazy potoczne .
W innych przypadkach różni się rodzaj rzeczowników morawskich i ogólnoczeskich .
