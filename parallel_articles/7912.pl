Smardz grubonogi
Smardz grubonogi ( `` Morchella crassipes '' ( Vent . )
Pers . )
– gatunek grzybów należący do rodziny smardzowatych ( `` Morchellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Morchella , Morchellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1797 r. Étienne Pierre Ventenat nadając mu nazwę `` Phallus crassipes '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1801 r. Christiaan Hendrik Persoon , przenosząc go do rodzaju `` Morchella '' .
