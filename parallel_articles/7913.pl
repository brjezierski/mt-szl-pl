Smardz wyniosły
Smardz wyniosły ( `` Morchella elata '' Fr . )
– gatunek grzybów należący do rodziny smardzowatych ( `` Morchellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Morchella , Morchellaceae , Pezizales , Pezizomycetidae , Pezizomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Niektóre synonimy :
Morfologia .
Wysokość 4–10 , rzadko do 15 cm .
Owocnik składa się z główki stanowiącej od 1/2 do 2/3 jego wysokości i trzonu .
Wewnątrz jest pusty .
Główka o barwie od czerwonobrązowej do czarnobrązowej , podobnej szerokości jak trzon , smukła , o kształcie od stożkowatego do dzwonkowatego z ostrym wierzchołkiem , z pionowymi żebrami i poprzecznymi listewkami .
Żebra i listewki tworzą ograniczenie znajdujących się między nimi alweoli .
Brzeg główki niepodwinięty .
Trzon o barwie od białej do bladoochrowożółtej , słabo pofałdowany i nieco oprószony .
Miąższ kruchy , woskowaty o przyjemnym zapachu i smaku .
