Morez
Morez – miejscowość i dawna gmina we Francji , w regionie Burgundia-Franche-Comté , w departamencie Jura .
W 2013 roku jej populacja wynosiła 5218 mieszkańców .
W dniu 1 stycznia 2016 roku z połączenia trzech ówczesnych gmin – Lézat , Morez oraz La Mouille – utworzono nową gminę Hauts de Bienne .
Siedzibą gminy została miejscowość Morez .
