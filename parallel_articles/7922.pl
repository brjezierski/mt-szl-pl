Moroni ( imię )
Moroni ( deseret rozmaicie , chociażby jako 𐐣𐐄𐐡𐐄𐐤𐐌 , 𐐣𐐃𐐡𐐃𐐤𐐌 , 𐐣𐐄𐐡𐐃𐐤𐐌 , 𐐣𐐃𐐡𐐄𐐤𐐌 czy 𐐤𐐄𐐡𐐄𐐤𐐌 ) – imię męskie występujące w wyznaniach należących do ruchu świętych w dniach ostatnich ( mormonów ) .
Pochodzi z Księgi Mormona , w której nosiły je dwie postacie .
Pojawiło się w źródłach już w początkach mormonizmu .
Popularne w zdominowanym przez świętych w dniach ostatnich stanie Utah .
Występuje wśród wyznających mormonizm Maorysów oraz w Brazylii i Ekwadorze , jak również w praktykującym małżeństwa pluralistyczne Fundamentalistycznym Kościele Jezusa Chrystusa Świętych w Dniach Ostatnich .
Znalazło odzwierciedlenie w szerszej mormońskiej kulturze .
Jako imię wywiedzione z pism świętych uznawane jest za powód do dumy .
Ma także wywierać pozytywny wpływ na życie duchowe noszącego je człowieka .
Pochodzenie .
Pochodzi z Księgi Mormona , jednego z pism świętych przynależnych do kanonu tej tradycji religijnej .
Nosiły je w tym tekście dwie kluczowe postacie .
