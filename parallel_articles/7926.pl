Krynica Morska
Krynica Morska ( niem .
`` Kahlberg '' , 1947–58 `` Łysica '' ) – miasto w województwie pomorskim , w powiecie nowodworskim , na Mierzei Wiślanej , między Zatoką Gdańską a Zalewem Wiślanym , przy drodze wojewódzkiej nr 501 .
Miejscowość turystyczna z portem morskim , czterema przystaniami morskimi i letnim kąpieliskiem .
Położenie .
Według danych z 1 stycznia 2011 roku powierzchnia gminy miejskiej wynosi 116,01 km² .
Krynica Morska stanowi 15,63 % powierzchni powiatu .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
Według danych z 2005 roku Krynica Morska ma obszar 102,04 km² , w tym : użytki rolne 1 % , użytki leśne 16 % .
Przyroda .
Obszar lądowy Krynicy Morskiej znajduje się w całości w Parku Krajobrazowym „ Mierzeja Wiślana ” i jednocześnie w Bałtyckim Systemie Obszarów Chronionych HELCOM MPA ( Marine Protected Area ) nr 83 Zalew Wiślany i Mierzeja Wiślana .
Funkcję uzupełniającą ochronę tego obszaru spełnia specjalny obszar ochrony siedlisk Zalew Wiślany i Mierzeja Wiślana PLH280007 .
