Mortsel ( stacja kolejowa )
Mortsel ( ned : `` Station Mortsel '' ) – stacja kolejowa w Mortsel , w prowincji Antwerpia , w Belgii .
Znajduje się na linii 27 Bruksela - Antwerpia .
Położona jest zaledwie kilkadziesiąt metrów od stacji Mortsel-Deurnesteenweg , położonej na linii 25 .
