Moryń
Moryń ( niem .
`` Mohrin '' ) – miasto w północno-zachodniej Polsce , w woj .
zachodniopomorskim , w powiecie gryfińskim .
Siedziba gminy miejsko-wiejskiej Moryń .
Położone nad jeziorem Morzycko .
31 grudnia 2012 r. miasto miało 1631 mieszkańców .
Nad jeziorem Morzycko zostało wyznaczone kąpielisko śródlądowe .
