Moses Lake
Moses Lake – miasto w Stanach Zjednoczonych , w stanie Waszyngton , którego ludność wynosi 20,366 mieszkańców ( wedle spisu z 2010 roku ) .
Jest to największa miejscowość w hrabstwie Grant , podobnie jak położone wokoło miasta jezioro Moses jest największe pośród naturalnych zbiorników wodnych hrabstwa ( pokrywa ponad 2600 ha ) .
