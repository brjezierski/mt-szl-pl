Mosonmagyaróvár
Mosonmagyaróvár ( ) – miasto w północno-zachodnich Węgrzech , w pobliżu granicy ze Słowacją i Austrią , w komitacie ( okręgu ) Győr-Moson-Sopron .
Stanowi ośrodek administracyjny powiatu Mosonmagyaróvár .
Leży przy ujściu rzeki Litawy do Dunaju .
W roku 2011 miasto liczyło 32 493 mieszkańców .
