Mostki ( rejon grodzieński )
Mostki ( biał .
`` Масткі '' , ros .
`` Мостки '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Jeziory .
W czasach zaborów uroczysko w pobliżu wsi Stryjówka w granicach Imperium Rosyjskiego , w guberni grodzieńskiej , w powiecie grodzieńskim .
W latach 1921–1939 wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Jeziory .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 75 osób , wszystkie były wyznania prawosławnego i deklarowali białoruską przynależność narodową .
Było tu 12 budynków mieszkalnych .
Miejscowość należała do parafii prawosławnej w Jeziorach .
