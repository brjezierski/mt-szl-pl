Mosul
Mosul ( arab .
الموصل , `` Al-Mawṣil '' ; kurd .
مووسڵ , `` Musll '' ) – miasto w północnym Iraku , stolica muhafazy Niniwa .
Mosul leży nad Tygrysem , około 350 km na północny zachód od Bagdadu ; jest największym miastem w kraju pod względem powierzchni po Bagdadzie i Basrze .
Historia .
Przed podbojem tych terenów przez muzułmanów w VII wieku , starożytne miasto Niniwa było przez wiele wieków chrześcijańskie , odkąd Asyryjczycy w I i II wieku przyjęli nową wiarę .
Na miejscu starożytnej osady w VII wieku Arabowie zbudowali miasto , które w VIII wieku stało się głównym miastem Mezopotamii .
W następnych stuleciach Mosul należał do lokalnych dynastii ( emirat Mosulu ) .
W latach 1535–1918 należał do Imperium Osmańskiego .
W latach 1918–1926 trwał turecko-brytyjski konflikt ( zwany mosulskim ) , po którym Mosul przyłączono do Iraku .
W 1959 roku doszło tu do zamieszek , podczas których starli się zwolennicy i przeciwnicy rządu .
