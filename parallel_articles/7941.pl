Moszczanka-Kolonia
Moszczanka-Kolonia – kolonia wsi Moszczanka w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Według danych na 2016 kolonia była zamieszkana przez 90 osób .
Moszczanka-Kolonia położona jest przy drodze powiatowej w kierunku północnym od pozostałej części Moszczanki .
Cechuje ją jednolity układ architektoniczny zabudowy mieszkalnej , powstałej przed II wojną światową .
Numeracja kolonii ( 1–17 ) jest oddzielna od reszty Moszczanki .
