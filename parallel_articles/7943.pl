Meczet w tekstach muzułmańskich .
Słowo „ masdżid ” powtarza się w Koranie , najczęściej w odniesieniu do sanktuarium Al-Kaba w mieście Mekka .
Koran stosuje termin „ masdżid ” w odniesieniu do miejsc kultu innych wyznań , w tym judaizmu i chrześcijaństwa .
W tym samym , ogólnym znaczeniu miejsca kultu słowo to wykorzystywane jest w hadisach .
Historia .
Wielkie wrota i wysokie wieże , zwane minaretami , są charakterystyczne dla meczetów .
Jednak meczety ewoluowały przez wieki i ich architektura dostosowywała się do tradycji narodów przyjmujących islam .
Pierwsze meczety .
Według przekazów muzułmańskich , pierwszym meczetem na świecie była Al-Kaba , zbudowana przez Abrahama na polecenie samego Boga .
Pierwszym meczetem zbudowanym przez muzułmanów był Meczet Kuba w Medynie .
Kiedy Mahomet mieszkał w Mekce , uważał Kaabę za najważniejsze miejsce kultu .
Prorok traktował Kaabę z ogromnym szacunkiem nawet gdy służyła ona za miejsce rytuałów ku czci demonów , dokonywanych przez pogańskich Arabów .
Kurajszyci próbowali nawet zabronić muzułmanom wstępu do Kaaby .
Po podboju Mekki w 630 roku , Mahomet przekształcił Kaabę w meczet , zwany odtąd Al-Masdżid al-Haram , czyli Świętym Meczetem .
Masdżid al-Haram był systematycznie powiększany przez kalifów , by pomieścić rosnącą liczbę wiernych przybywających na doroczną pielgrzymkę ( `` hadżdż '' ) .
Obecny wygląd świątynia ta uzyskała w 1577 za panowania osmańskiego sułtana Selima II .
