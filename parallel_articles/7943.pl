Meczet
Meczet ( `` masdżid '' ; l.mn .
`` masadżid '' ) – miejsce kultu muzułmańskiego .
Słowo „ meczet ” oznacza dowolny budynek , w którym oddaje się cześć Bogu , niezależnie od jego architektury .
Podstawowym celem istnienia meczetów jest umożliwienie muzułmanom wspólnej rytualnej modlitwy ( `` salat '' ) .
Pierwsze meczety , takie jak Meczet Kuba i Masdżid an-Nabawi , powstały w VII wieku i stanowiły po prostu wyznaczone miejsca pod gołym niebem .
Dziś większość meczetów posiada wyrafinowane kopuły i minarety .
Etymologia .
Arabskie słowo `` masdżid '' jest rzeczownikiem miejsca utworzonym od czasownika `` sadżada '' ( oznaczającego „ zginać się ” lub „ klękać ” ) , co jest odniesieniem do prostracji w czasie muzułmańskiej modlitwy .
