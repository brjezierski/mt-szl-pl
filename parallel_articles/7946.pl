Motey-sur-Saône
Motey-sur-Saône – miejscowość i dawna gmina we Francji , w regionie Burgundia-Franche-Comté , w departamencie Górna Saona .
W 2016 roku populacja ówczesnej gminy wynosiła 30 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia dwóch ówczesnych gmin – Motey-sur-Saône oraz Seveux – powstała nowa gmina Seveux-Motey .
Siedzibą gminy została miejscowość Seveux .
