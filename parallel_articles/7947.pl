Motherwell
Motherwell ( gael .
`` Tobar na Màthar '' ) – miasto w Szkocji , ośrodek administracyjny hrabstwa North Lanarkshire .
Ok. 30 tysięcy mieszkańców .
Siedziba pierwszoligowego klubu piłkarskiego Motherwell F.C .
Historia .
Na początku XIX wieku Motherwell było małą wioską liczącą około 600 osób .
Zmieniło się to w drugiej połowie XIX wieku .
Wraz z powstaniem kolei w 1848 roku do miejscowości przybyły pieniądze i przemysł .
W 1881 roku , David Colville otworzył hutę .
Wkrótce potem nadano Motherwell prawa miejskie , a populacja wzrosła do 13800 ludzi .
Na początku XX wieku , miasto stało się znaczącym ośrodkiem przemysłowym , produkującym części mostów , amunicję czy też tramwaje .
W 1930 roku większość produkcji stali w Szkocji odbywało się w Motherwell , i należało do rodziny Colville .
W 1959 rodzina Colville została przekonana przez rząd do rozpoczęcia pracy nowych hut pod nazwą Ravenscraig .
Po kilku latach , Ravenscraig produkowało więcej niż milion ton stali rocznie .
