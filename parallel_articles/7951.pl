Motycz-Józefin
Motycz-Józefin – kolonia w Polsce położona w województwie lubelskim , w powiecie lubelskim , w gminie Konopnica .
W wyniku III rozbioru Polski , do którego doszło w 1795 roku , teren dzisiejszego Motycza-Józefina znalazł się na obszarze zaboru austriackiego .
W 1809 wieś została włączona do Księstwa Warszawskiego .
W 1815 znalazła się w Królestwie Kongresowym w zaborze rosyjskim .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa lubelskiego .
Miejscowość stanowi sołectwo gminy Konopnica .
Według Narodowego Spisu Powszechnego z roku 2011 kolonia liczyła 238 mieszkańców .
