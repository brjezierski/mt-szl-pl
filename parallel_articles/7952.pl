Motörhead ( album )
Motörhead – pierwszy album studyjny zespołu Motörhead , wydany 24 września 1977 roku nakładem wytwórni muzycznej Chiswick Records .
Został nagrany w Escape Studios pomiędzy kwietniem a majem 1977 roku .
