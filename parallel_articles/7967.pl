Mołdawia
Mołdawia , Republika Mołdawii ( ; ) – państwo europejskie położone na terenach historycznej Besarabii oraz częściowo na lewym brzegu Dniestru .
Leżące na lewym brzegu Dniestru tzw .
Naddniestrze , niewielka część Mołdawii zamieszkana w znacznej mierze przez Rosjan i Ukraińców , po upadku Związku Radzieckiego ogłosiło secesję i od czasu krótkiej wojny domowej w 1992 funkcjonuje jak niepodległe państwo ze stolicą w Tyraspolu ( `` Naddniestrzańska Republika Mołdawska '' lub błędnie `` Mołdawska Republika Naddniestrza '' ) , które jednak nie jest uznawane na arenie międzynarodowej .
Od tego czasu toczą się bezowocne rozmowy zjednoczeniowe , w których pośredniczy m.in .
OBWE .
Władze mołdawskie opowiadają się za zjednoczeniem „ asymetrycznym ” , w ramach którego Naddniestrze otrzyma szeroką autonomię , władze w Tyraspolu dążą zaś do zjednoczenia „ symetrycznego ” , w którym obie części Mołdawii będą miały takie same prawa .
Część Mołdawian opowiada się za zjednoczeniem z Rumunią .
W zorganizowanym w 1994 roku referendum dotyczącym zjednoczenia poparcie dla niego było minimalne – 94,4 % obywateli opowiedziało się za niezależnością , jednak sondaż zorganizowany w 2008 roku , po 17 latach niepodległości , wskazał , że już 29 % Mołdawian popiera ideę zjednoczenia .
W 2021 roku poparcie dla zjednoczenia wzrosło do 43,9 % .
Według CIA Mołdawia była najbiedniejszym państwem Europy w 2012 roku .
Nazwa .
Nazwa `` Mołdawia '' została przyjęta dla państwa od rozleglejszego regionu , dawnej jednostki politycznej , posiadającego taką samą nazwę .
Region nazwany zaś został od jednej z jego głównych rzek – Mołdawy .
Nazwa łacińska tego regionu – `` Moldavia '' – pojawia się w tej formie już w `` Annales seu cronicae incliti Regni Poloniae '' Jana Długosza z lat 1455–1480 .
Polska nazwa regionu w formie `` Mołdawija '' notowana jest już w publikacjach z 1609 i 1633 r. ; prawdopodobnie od polskiej formy nazwy utworzona została nazwa rosyjska `` Молдавия '' ( w transkrypcji : `` Mołdawija '' ) .
Czasem używana jest niezalecana przez Komisję Standaryzacji Nazw Geograficznych poza Granicami RP nazwa `` Mołdowa '' .
Komisja zaleca wyłącznie formy `` Mołdawia '' i `` Republika Mołdawii '' .
Geografia .
Mołdawia graniczy z Ukrainą i Rumunią .
Warunki naturalne .
Mołdawia zajmuje międzyrzecze Dniestru i Prutu ( Besarabia ) oraz wąski pas na lewym brzegu Dniestru ( Naddniestrze ) .
Powierzchnia równinna , pocięta głębokimi dolinami rzecznymi i wąwozami .
Występuje kras .
Średnia wysokość terenu to 147 m. Ponad 75 % powierzchni na wysokości poniżej 200 m .
Większość powierzchni państwa zajmuje Wyżyna Besarabska , w której skład wchodzą m.in .
