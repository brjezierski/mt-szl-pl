) , Wyżyna Środkowomołdawska ( 430 m n.p.m . )
i Wyżyna Tyhecka ( 301 m n.p.m . ) .
Południowych granic państwa sięga Nizina Czarnomorska .
Klimat umiarkowany kontynentalny , na południu suchy , ze stosunkowo łagodną zimą i długim gorącym latem .
Średnia temperatura w styczniu od –5 °C na północy do –3 °C na południu , w lipcu odpowiednio 19 °C i 22 °C ( w Kiszyniowie absolutne minimum –30 °C , maksimum 39 °C ) .
Roczna suma opadów 400 mm na południu do 560 mm na północy .
Opady głównie wiosenno-letnie o charakterze ulew .
