Mołoczanśk
Mołoczanśk ( ukr .
Молочанськ ) – miasto w południowo-wschodniej części Ukrainy pod okupacją rosyjską , w obwodzie zaporoskim .
Historia .
Wieś została założona w 1803 roku w guberni taurydzkiej .
Status miasta od 1938 .
W 1950 r. wybudowano , w 1954 r. zbudowano fabrykę mebli .
W 1989 liczyła 9096 mieszkańców .
W 2013 liczyła 6980 mieszkańców .
