Mołodeczno ( stacja kolejowa )
Mołodeczno ( biał .
Чыгуначная станцыя ў Маладзечне ; ros .
Железнодорожный вокзал в Молодечно ) – stacja kolejowa w Mołodecznie na Białorusi otwarta w 1907 roku .
Historia .
Stacja Mołodeczno została otwarta w XIX w. na drodze żelaznej libawsko-romeńskiej , pomiędzy stacjami Zalesie i Usza .
Budynek dworca wzniesiono w stylu modernizmu w 1907 na miejscu dawnego drewnianego dworca pochodzącego z 1873 .
Był obiektem parterowym , murowanym , zbudowanym na planie prostokąta .
Charakteryzował się symetryczną budową , w fasadzie głównej i przybudowach przecięty ryzalitami z oknami w formie łuku .
