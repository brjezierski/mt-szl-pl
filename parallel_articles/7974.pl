Mosina ( stacja kolejowa )
Mosina – stacja kolejowa w Mosinie , w województwie wielkopolskim , w Polsce .
Opis .
Według klasyfikacji PKP ma kategorię dworca aglomeracyjnego .
Poza nią , w Mosinie mieści się też nieczynny obecnie przystanek Mosina Pożegowo .
Aktualnie tutaj zatrzymują się wszystkie pociągi przewoźnika Przewozy Regionalne .
Przez stację przebiega szlak pieszy nr 3583 łączący Rogaliński Park Krajobrazowy z Wielkopolskim Parkiem Narodowym .
Pasażerowie .
W roku 2017 wymiana pasażerska wyniosła 1,0-1,5 tys .
