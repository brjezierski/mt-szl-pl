Mrówkowate
Mrówkowate ( Formicidae ) – rodzina owadów należąca do rzędu błonkówek , podrzędu trzonkówek .
Bardzo rozpowszechnione , występują praktycznie pod każdą szerokością geograficzną , tworzą społeczności kastowe żyjące w gniazdach .
Nazwy naukowe posiada 16 000 współcześnie występujących gatunków mrówek ; szacuje się jednak , że w świat zamieszkuje 20 000 gatunków tych owadów .
W Polsce występują 103 gatunki .
Opis ogólny .
U mrówkowatych , podobnie jak u pszczołowatych , prócz samców i samic występuje również kasta robotnic , czyli samic o niedorozwiniętych narządach rozrodczych .
U niektórych gatunków bywa nawet po kilka postaci robotnic .
Robotnice opatrzone bardzo silnymi żuwaczkami i wielkimi głowami noszą nazwę żołnierzy .
Niekiedy pomiędzy samicami rozwiniętymi płciowo a robotnicami właściwymi występują formy pośrednie .
Samce i samice są prawie u wszystkich gatunków skrzydlate , robotnice zawsze bezskrzydłe .
Głowa .
Głowa mrówek jest zawsze silnie rozwinięta , a wśród narządów gębowych specjalnie dobrze wykształcone są żuwaczki , będące głównym narządem aparatu gębowego .
Oczy i przyoczka mogą być rozmaicie rozwinięte u poszczególnych gatunków , u robotnic ulegają niekiedy znacznemu uwstecznieniu .
Czułki zawsze dobrze rozwinięte , najczęściej zgięte , biczykowate .
Odnóża .
Odnóża zgrubione , na przednich występują często specjalne haczyki do czyszczenia czułków i otworu gębowego .
Tułów i odwłok .
Tułów o pierścieniach ściśle z sobą zespolonych , a u robotnic nawet częściowo pozlewanych .
Odwłok ruchomo zestawiony z tułowiem , dzięki przekształceniu jednego lub dwóch pierścieni w cienki stylik .
Żądła zwykle brak , gruczoł jadowy dobrze wykształcony ; jad zawiera zawsze mniejsze lub większe ilości kwasu mrówkowego .
Pancerz chitynowy zwykle mocny , często o charakterystycznym dla poszczególnych gatunków urzeźbieniu , zwłaszcza na głowie i tułowiu .
Ubarwienie przeważnie ciemne , czarne , brunatne lub żółte , rzadziej pstre .
Organizacja społeczna .
Mrówki tworzą złożone społeczności o wyraźnej strukturze hierarchicznej i ściśle przydzielonych obowiązkach .
Robotnice mają do spełnienia funkcje usługowe .
Budują gniazdo , bronią go , zbierają pożywienie , opiekują się potomstwem .
Rolą samców jest zaplemnienie królowej ; po tym giną .
Zadaniem królowej jest składanie jaj , z których po przeobrażeniu powstają nowe mrówki , a także regulacja działania społeczności za pomocą feromonów .
Mrówki żyją w zorganizowanej społeczności , w której każda z nich odgrywa przypisaną sobie rolę , przyczyniając się do właściwego funkcjonowania całego mrowiska .
Setki tysięcy mrówek żyją w społeczności pod kopcem z nagromadzonych igieł sosnowych i resztek roślin .
Cały czas pracując , mieszają one glebę , spulchniają ją i wzbogacają .
Gniazda .
Większość gatunków zamieszkuje zbudowane przez siebie gniazda , zwane powszechnie mrowiskami .
Mogą one być bardzo zróżnicowane , niekiedy ten sam gatunek może budować różne typy mrowisk , zależnie od warunków środowiska , choć na ogół sposób budowania mrowisk jest charakterystyczny dla poszczególnych gatunków .
Do częstych typów mrowisk należą gniazda podziemne , złożone z licznych komór i korytarzy , nad którymi pewne gatunki wznoszą jeszcze kopce naziemne ( kopiec ochronny ) z igliwia , patyczków i piasku , jak pospolita w polskich lasach mrówka rudnica .
Bardzo wiele gatunków zakłada mrowiska w drewnie drzew żywych lub butwiejącym , przy czym ściany komór i korytarzy sporządzone są z przypominającej tekturę , przeżutej masy drzewnej ; takie gniazda w starych pniach przy ziemi zakłada na przykład pospolita w Polsce mrówka hurtnica .
Bardzo ciekawe są gniazda afrykańskich i indyjskich mrówek-tkaczy ( m.in .
z rodzaju `` Oecophylla '' ) : są one budowane w koronach drzew z liści połączonych przędzą wydzielaną przez larwy tych mrówek ; przy budowie takiego gniazda robotnice trzymają larwy w żuwaczkach i posługują się nimi jak czółenkami tkackimi .
Niektóre gatunki nie budują własnych mrowisk , lecz mieszkają w mrowiskach innych gatunków jako pasożyty .
Są też mrówki niemające w ogóle stałych mrowisk , zakładające tylko gniazda czasowe , a większą część życia koczujące , jak na przykład niezwykle drapieżne amerykańskie mrówki koczujące z rodzaju `` Eciton '' .
W mrowiskach żyją prócz mrówek jeszcze inne zwierzęta ( myrmekofile ) , których tryb życia jest ściśle związany z mrówkami .
Mrowiska mogą istnieć przez wiele lat .
Powstawanie nowych gniazd .
Świeżo zaplemnione królowe starają się zapewnić sobie korzystne warunki do wydania potomstwa .
Zakładanie nowego społeczeństwa przez królową odbywa się na 2 sposoby : zależnie i niezależnie .
Pierwszy typ , jeśli związany jest z czasowym pasożytnictwem , polega na tym , że wnika ona do społeczeństwa obcego gatunku ( posiadając specjalne przystosowania do tłumienia agresji atakujących ją z początku mrówek ) i zabija jego królową .
Miejscowe robotnice odtąd opiekują się potomstwem obcej mrówki .
W miarę jak wymierają , ich miejsce zajmuje gatunek pasożyta .
W końcu gniazdo powraca do jednogatunkowości ( choć z innym gatunkiem ) .
Mrówki zakładające gniazda niezależnie , samodzielnie kopią w ziemi jamki ( zalążki przyszłego gniazda ) i opiekują się potomstwem do czasu wylęgnięcia pierwszych dorosłych robotnic .
Schemat tworzenia nowego gniazda ze względu na liczbę królowych rozpoczynających rozwój kolonii w mrowisku :
Mieszkańcy gniazd .
Większość mieszkańców mrowiska stanowią robotnice , których liczba może dochodzić do kilkuset tysięcy .
Prócz tego znajduje się w nim zazwyczaj kilka samic zapłodnionych ( rzadziej jedna ) , znoszących jaja .
Po przeistoczeniu się z poczwarek , młode samce i samice odbywają lot godowy , w czasie którego osobniki uskrzydlone obu płci unoszą się w powietrze i tam odbywają kopulację .
Kopulacja i rozwój larw .
Po kopulacji samce giną , a zapłodnione samice tracą skrzydła , zostają zabrane przez robotnice swego gatunku do jakiegoś istniejącego już mrowiska , lub zakładają nowe .
W tym przypadku samica znosi kilkanaście jaj i sama hoduje z nich pierwsze robotnice , które następnie obejmują przypisane im czynności polegające przede wszystkim na budowie i utrzymywaniu w porządku gniazda , zdobywaniu pokarmu i opiece nad samicą znoszącą jaja i młodym pokoleniem .
Larwy mrówek mają postać beznogich czerwi pokrytych drobnymi włoskami .
Mają one gruczoły przędne i przed przepoczwarzeniem otaczają się kokonem .
Takie spoczywające w kokonach poczwarki mrówek znane są pod nazwą mrówczych jaj .
Robotnice nie tylko dostarczają pokarm czerwiom , ale również oczyszczają je i przenoszą z komory do komory lub wynoszą na powietrze , zależnie od zmian temperatury i wilgotności .
W podobny sposób opiekują się jajami i poczwarkami .
Pożywienie .
Pożywienie mrówek to pokarm zarówno roślinny jak i zwierzęcy , na ogół z przewagą tego ostatniego .
Istnieją mrówki roślinożerne i wszystkożerne .
Z punktu widzenia człowieka duża liczba gatunków mrówek to owady pożyteczne , ponieważ niszczą , zwłaszcza w lasach , larwy wielu gatunków szkodników .
Mrówki południowoamerykańskie z rodzaju `` Atta '' i niektórych pokrewnych rodzajów żywią się grzybami rozwijającymi się w ich mrowiskach , na ciastowatej masie , przyrządzonej przez robotnice z przeżutych liści .
Dla zdobycia potrzebnego na to materiału mrówki te odbywają stałe wyprawy na drzewa i krzewy , gdzie wycinają z liści żuwaczkami krążki dochodzące do 2 cm średnicy , które następnie przenoszą do gniazda .
