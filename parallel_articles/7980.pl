Mrągowo ( stacja kolejowa )
Mrągowo – nieczynna stacja kolejowa w Mrągowie , w województwie warmińsko-mazurskim , w Polsce .
Dworzec wybudowany w latach 60 .
XX wieku .
Od roku 2010 stacja nieczynna w ruchu pasażerskim , czynna w ruchu towarowym jedynie w kierunku Czerwonki .
W okresie letnim obowiązywania rozkładu jazdy pociągów 1999/2000 na stacji zatrzymywały się pociągi międzynarodowe `` Mare Balticum '' rel .
Ełk – Berlin-Lichtenberg .
