Mtarfa
Mtarfa ( lub `` Imtarfa '' ) – jedna z jednostek administracyjnych na Malcie .
Mieszka tutaj 2 572 osób .
Tereny Mtarfy do 2000 roku były częścią Rabatu .
Znajdują się tu Chadwick Lakes , zespół stawów i tam .
Sport .
W miejscowości funkcjonuje klub piłkarski Mtarfa F.C .
Powstał w 2006 roku .
Obecnie gra w ostatniej maltańskiej lidze .
Położony tutaj jest stadion Rabat Ajax Football Ground , na którym mecze rozgrywa drużyna z sąsiedniej gminy - Rabat Ajax .
