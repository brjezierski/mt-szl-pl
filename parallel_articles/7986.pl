13 września lub 7 czerwca 1942 w Syrcie , zm .
20 października 2011 roku tamże ) – libijski pułkownik , polityk i ideolog , w latach 1969–2011 sprawował władzę autorytarną jako Przywódca Rewolucji 1 września .
W 1969 roku przeprowadził zamach stanu w którym obalił monarchię .
Proklamował utworzenie „ Libijskiej Republiki Arabskiej ” , a władzę objęła Rada Dowódcza Rewolucji .
W 1970 roku desygnowany na premiera i dowódcę sił zbrojnych , urzędy te formalnie sprawował do 1972 .
W tym czasie przeprowadził islamizację praw i obyczajów .
Znacjonalizował niearabską własność i zachodnich towarzystw naftowych .
Zlikwidował też amerykańskie i brytyjskie bazy wojskowe .
W 1977 państwo zmieniło nazwę na „ Wielka Arabska Libijska Dżamahirijja Ludowo-Socjalistyczna ” .
Zmiana ta była efektem jego programu ideowego zawartego w tzw .
Zielonej książce , w której połączył fundamentalistyczne tradycje islamskie z socjalizmem arabskim .
W latach 1977–1979 pełnił urząd sekretarza generalnego Powszechnego Kongresu Ludowego .
W marcu 1979 roku zrezygnował ze wszystkich funkcji państwowych ; w rzeczywistości sprawował dyktatorską władzę jako tzw .
Przywódca Rewolucji 1 Września i zwierzchnik sił zbrojnych .
W polityce międzynarodowej entuzjastycznie odnosił się do zjednoczenia świata arabskiego .
Głównym celem jego polityki była likwidacja Izraela .
Utrzymywał bliskie relacje ze Związkiem Radzieckim .
Prowadził agresywną politykę i angażował się w konflikty w między innymi Czadzie , Ugandzie i Sudanie .
Środki na prowadzenie wojen czerpał z eksploatacji złóż naftowych .
W latach 80. oskarżany przez Zachód o sponsorowanie terroryzmu .
Po atakach lotnictwa amerykańskiego złagodził nieco swoją politykę .
W 2011 roku nie zgodził się oddać władzy w następstwie społecznych protestów .
Doprowadziło to do wybuchu wojny domowej .
Zginął w strzelaninie pomiędzy powstańcami a wiernymi mu siłami .
