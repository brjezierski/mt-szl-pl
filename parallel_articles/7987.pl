Much Wenlock
Much Wenlock – miasto w Wielkiej Brytanii , w Anglii , w hrabstwie Shropshire .
W 1468 roku w ( Much ) Wenlock utworzono okręg wyborczy do parlamentu angielskiego .
W latach 1707–1800 był okręgiem parlamentu Wielkiej Brytanii , a w latach 1801–1885 ( do roku zniesienia ) był okręgiem reprezentowanym w Izbie Gmin Zjednoczonego Królestwa ( zob .
historia Wielkiej Brytanii ) .
W 2001 roku miasto to zamieszkiwało 2 605 osób [ `` '' ] .
