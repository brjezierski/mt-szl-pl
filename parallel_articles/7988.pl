Morfologia i rozwój .
Standardowo hoduje się go na agarze YPG i MMC .
Do wzrostu wymaga obecności światła , które aktywuje biosyntezę karotenoidów i bezpłciowe rozmnażanie .
Dominującą formą wzrostu jest grzybnia strzępkowa , ale w określonych warunkach może również rosnąć jako drożdże .
Po 3 dniach od inkubacji na agarze ziemniaczano-dekstrozowym ( PDA ) osiąga średnicę 8,2 cm w 30° C i 6,0 cm w 37° C , przy temperaturze 40° C przestaje rosnąć .
Kolonie na PDA w 30° C były początkowo żółtawe , a potem stały się żółtawo brązowe .
Tworzy kuliste , żółtawobrązowe zarodnie o średnicy od 30 do 67 μm .
