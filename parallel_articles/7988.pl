Mucor circinelloides
Mucor circinelloides Tiegh .
– gatunek grzybów należący do rodziny pleśniakowatych ( `` Mucoraceae '' ) .
Jest potencjalnie chorobotwórczy , u ludzi wywołuje grzybicę skóry .
