Morfologia i rozwój .
Na podłożu hodowlanym tworzy kremowo-białe kolonie o wysokości do 1,8 cm .
Sporangiofory gładkie z gutulami w środku , o szerokości 8–12 μm , zwykle proste , później rozgałęziające się .
Zarodnie kuliste , jasnobrązowe , o średnicy 43,7–60 µm i rozpływających się ścianach , po których pozostaje kołnierz .
Kolumelle prawie kuliste lub elipsoidalne , 21–30 × 20–28 μm , gładkie , z gutulami w środku .
Sporangiospory elipsoidalne , różnej wielkości 4,0–6,4 × 2–3,3 μm , o stosunku szerokości do długości 1,7–2 : 1 , gładkie , z gutulami na dwóch końcach .
Jest heterotaliczny .
Zygosporangia początkowo jasnobrązowe , w stanie dojrzałym ciemnobrązowe , kuliste , o średnicy 64–90 μm średnicy , na zygoforach o zróżnicowanej średnicy ; większe 20–32 μm , mniejsze 8–11 μm średnicy .
