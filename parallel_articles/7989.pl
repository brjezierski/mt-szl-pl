Mucor hiemalis
Mucor hiemalis Wehmer – gatunek grzybów należący do rodziny pleśniakowatych ( `` Mucoraceae '' ) .
Jeden z najczęściej występujących pleśniaków , grzyb pasożytniczy , u ludzi i zwierząt powodujący groźne choroby .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mucor , Mucoraceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Ma 21 synonimy .
