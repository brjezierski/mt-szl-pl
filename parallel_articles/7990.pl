Mucor indicus
Mucor indicus Lendn .
– gatunek grzybów należący do rodziny pleśniakowatych ( `` Mucoraceae '' ) .
Jest jednym z częściej występujących pleśniaków .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mucor , Mucoraceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Synonim : `` Zygorhynchus indicus '' ( Lendn . )
Arx 1982 :
Morfologia i rozwój .
Grzyb saprotroficzny .
Na różnych podłożach stałych , takich jak ziemniaki , agar , moszcz agarowy tworzy kolonie o delikatnej , jasnoróżowo-szarej barwie .
Każdy sporangiofor załamuje się pod bardzo ostrym kątem i kończy się małym , różowawym sporangium o średnicy 40–44 µm .
Jego błona łatwo się rozpada , ale czasami , jeśli są to młode zarodnie , występuje w postaci strzępów lub kołnierzyków .
Kolumelle różnią się znacznie kształtem i rozmiarem .
Rzadko są kuliste ( 26 µm ) , najczęściej owalne o rozmiarze 20 na 24 µm .
