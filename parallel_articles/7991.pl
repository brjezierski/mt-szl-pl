Pleśniak biały
Pleśniak biały ( `` Mucor mucedo '' Fresen . )
– gatunek grzybów należący do rodziny pleśniakowatych ( `` Mucoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mucor , Mucoraceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Niektóre synonimy nazwy naukowej :
Rozmnażanie .
Jest komórczakiem , to znaczy , że zbudowany jest z jednej , ale wielojądrowej komórki tworzącej strzępkę przerastającą podłoże .
Ze strzępki tej wyrastają wysokie trzonki zarodnionośne zwane sporangioforami .
Na ich szczycie powstają kuliste , czarne zarodnie ( sporangia ) , w których wytwarzane są bezpłciowo zarodniki ( sporangiospory ) .
Są one haploidalne i zróżnicowane płciowo na dwa typy , oznaczane zazwyczaj jako ( + ) i ( - ) .
Po pęknięciu zarodni zarodniki wysypują się i mogą być roznoszone przez wiatr , wodę lub zwierzęta .
W sprzyjających warunkach kiełkują wytwarzając rozgałęzione , wielojądrowe strzępki o haploidalnej liczbie chromosomów ( cenocyty ) .
Strzępki te morfologicznie nie różnią się one od siebie , są jednak zróżnicowane płciowo .
Zarówno strzępki ( + ) , jak i strzępki i ( - ) mogą wytwarzać zarodnie .
