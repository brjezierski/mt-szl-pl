Pleśniak groniasty
Pleśniak groniasty ( `` Mucor racemosus '' Fresen ) – gatunek grzybów należący do rodziny pleśniakowatych ( `` Mucoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mucor , Mucoraceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Na początku 2021 w serwisie Species Fungorum odnotowano 26 synonimów .
Niektóre z nich :
W polskiej literaturze bywa określany jako pleśniak groniasty .
Morfologia i rozwój .
Grzyb mikroskopijny powszechnie występujący w glebie i na butwiejącym materiale roślinnym .
Znajdywano go także w licznych produktach spożywczych .
Jest saprotrofem , odżywiającym się martwą materią organiczną .
W zależności od warunków środowiska może występować w postaci strzępkowej ( pleśniowej ) lub drożdżowej .
Szybko rośnie zarówno w warunkach tlenowych , jak i beztlenowych .
W powietrzu tworzy grubą szarą grzybnię , która stopniowo staje się brązowa lub czarna wraz z nagromadzeniem zarodni , które zawierają liczne ciemne sporangiospory .
Zarodnie przeważnie mają 50–60 μm szerokości , a spory są mniej więcej kuliste .
