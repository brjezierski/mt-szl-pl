Mucor ramosissimus
Mucor ramosissimus Samouts .
– gatunek grzybów należący do rodziny pleśniakowatych ( `` Mucoraceae '' ) .
Grzyb chorobotwórczy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mucor , Mucoraceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Po raz pierwszy został wyizolowany z gleby w Rosji przez M.M .
Samoutsevitcha w 1927 r. Występuje również w Polsce .
Morfologia i rozwój .
Kolonie `` Mucor ramosissimus '' na podłożu MEA ograniczone , szarawe .
Sporangiofory hialinowe , do 2 mm wysokości , 18 µm szerokości , lekko chropowate , zwężające się ku wierzchołkowi , wielokrotnie sympodialnie rozgałęzione .
Sporangia czarniawe , kuliste do grzbietowo spłaszczonych , o średnicy do 80 µm , z trwałymi ścianami ; Kolumelle do 40–50 µm długości , nieobecne w małych zarodniach .
Sporangiospory o gładkich ścianach , kuliste do szeroko elipsoidalnych , 5–8 × 4,5–6,0 ( –7 ) um .
Oidia obecne w strzępkach substratowych .
Brak chlamydospor .
Maksymalna temperatura wzrostu 36° C. Asymilacja etanolu ujemna , azotanów dodatnia .
Charakterystycznymi cechami `` M. ramosissimus '' , które pozwalają go odróżnić od innych gatunków z rodzaju `` Mucor '' są : liczne zarodnie pozbawione kolumelli , krótkie , wyprostowane sporangiofory wielokrotnie rozgałęziające się sympodialnie , twarde i trwałeściany zarodni , liczne oidia w łańcuchach , kolonie wyjątkowo niskie i ograniczony wzrost w 36 stopniach C .
