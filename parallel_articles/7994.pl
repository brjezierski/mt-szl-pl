Muhos
Muhos – gmina w Finlandii , w regionie Pohjois-Pohjanmaa , w bezpośrednim sąsiedztwie Oulu .
Powierzchnia wynosi 797,39 km² , z czego 13,78 km² stanowi woda .
Populacja wynosi 8 857 osób , co sytuuje ją na 120. miejscu wśród gmin fińskich ( 2011 ) .
Przez Muhos przepływa rzeka Oulujoki , do której , również na terenie gminy , uchodzi Muhosjoki .
Na Oulujoki znajduje się elektrownia wodna Pyhäkoski o najwyższym spadku wody w Finlandii ( 32,4 m ) .
Budynek elektrowni zaprojektował architekt Aarne Ervi .
