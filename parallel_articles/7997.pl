Mullingar
Mullingar ( irl .
`` An Mulieann gCearr '' ) – miasto i ośrodek administracyjny hrabstwa Westmeath w środkowej Irlandii .
Atrakcje turystyczne .
Głównymi atrakcjami miasta są otaczające je jeziora : Lough Owel , Lough Lene oraz Lough Ennell .
Popularnym miejscem wycieczek turystycznych jest Belvedere House and Gardens – rezydencja znajdująca się w pobliżu miasta .
W mieście znajduje się katedra diecezji Meath Christ the King Mullingar .
