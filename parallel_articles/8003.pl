Murfreesboro ( Tennessee )
Murfreesboro – miasto w hrabstwie Rutherford w stanie Tennessee , USA .
Miasto znajduje się w środkowej części stanu Tennessee .
Jest jednym z najszybciej rozrastających się miast w Stanach Zjednoczonych pod względem liczby ludności .
Od 1990 do 2000 liczba ludności zwiększyła się o 50 % , z 46 000 do 69 000 .
W 2005 było ponad 87 000 mieszkańców .
