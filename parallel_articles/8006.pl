Murray Bridge
Murray Bridge – piąte co do wielkości miasto stanu Australia Południowa , położone nad rzeką Murray , ok. 80 km od Adelaide .
Według spisu ludności z 2006 , liczyło 14048 mieszkańców .
W gospodarce okolicy dominuje rolnictwo : hodowla bydła , trzody i drobiu , a także uprawa zbóż i warzyw .
Siedziba diecezji anglikańskiej .
