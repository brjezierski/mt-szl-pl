Mustafa Kemal Atatürk
Mustafa Kemal Atatürk ( ur .
1881 w Salonikach , zm .
10 listopada 1938 w Stambule ) – turecki polityk , wojskowy , Mareşal ( Marszałek Turcji ) , mąż stanu , współtwórca i pierwszy prezydent Republiki Turcji od roku 1923 do śmierci w 1938 roku .
W trakcie I wojny światowej był jednym z ważniejszych dowódców armii Imperium Osmańskiego .
Po klęsce w wojnie sprzeciwił się rozbiorom Turcji i stanął na czele wojny o niepodległość .
W niepodległej Turcji objął urząd prezydenta , przeprowadzając szereg reform politycznych , gospodarczych i kulturalnych , przekształcając Imperium Osmańskie w nowoczesne i świeckie państwo narodowe .
Jego działania dały początek ideologii kemalistycznej .
Uważany jest za jedną z najważniejszych postaci w historii Turcji i XX wieku .
Życiorys .
Wczesne życie .
Urodził się najprawdopodobniej w 1881 roku w Salonikach na terenie ówczesnego Imperium Osmańskiego jako syn Zübeydy Hanım i Aliego Rızy Efendiego .
Jego matka zajmowała się domem , ojciec był oficerem milicji i przedsiębiorcą .
Jedyną siostrą Kemala , która przeżyła dzieciństwo , była Makbule Atadan ( dożyła roku 1956 ) .
Rodzina Kemala jest różnie opisywana w źródłach .
Według Andrew Mango rodzina należała do muzułmańskiej klasy średniej mówiącej w języku tureckim .
Część źródeł wskazuje na to , że Kemal wśród najbliższych przodków miał osoby pochodzenia albańskiego bądź słowiańskiego ( na co wskazują jego cechy wyglądu – blada cera , blond włosy i niebieskie oczy ) , niektóre sugerują nawet , że etnicznym Albańczykiem był sam Ali Rıza .
Po urodzeniu otrzymał imię Mustafa .
Drugie imię Kemal ( oznaczające perfekcję lub dojrzałość ) nadał mu jego nauczyciel matematyki Üsküplü Mustafa Efendi lub przyjął on je sam na cześć nacjonalistycznego poety Namıka Kemala .
We wczesnej młodości uczęszczał do szkoły religijnej , gdzie pomimo niechęci samego Mustafy , wysłała go matka .
Za namową ojca porzucił religijną edukację i rozpoczął naukę w prywatnej szkole o bardziej świeckim charakterze .
Po ukończeniu szkoły , bez konsultacji z synem , rodzice wysłali go do szkoły wojskowej w Salonikach , gdzie naukę rozpoczął w roku 1893 .
W 1896 roku rozpoczął naukę w Wyższej Szkole Wojskowej w Bitoli , a od 1896 roku kontynuował naukę na studiach w Osmańskiej Akademii Wojskowej w Stambule .
Później ukończył Osmański College Wojskowy ( 1905 rok ) .
Kariera wojskowa .
Wkrótce po ukończeniu studiów został zatrzymany przez policję za działalność antymonarchistyczną .
Kilka miesięcy spędził w więzieniu , z którego został wypuszczony po interwencji Rızy Paszy , byłego dyrektora szkoły , do której Kemal uczęszczał .
Po wyjściu na wolność został w stopniu kapitana przydzielony do Piątej Armii z siedzibą w Damaszku ( obecnie w Syrii ) .
Na miejscu dołączył do tajnej grupy rewolucyjnej postępowych oficerów kierowanych przez Mustafę Cantekina .
Grupa nosiła nazwę `` Vatan ve Hurriyet '' – `` Ojczyzna i Wolność '' .
