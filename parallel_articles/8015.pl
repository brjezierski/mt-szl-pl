Muswellbrook ( stacja kolejowa )
Muswellbrook – stacja kolejowa w miejscowości Muswellbrook , w stanie Nowa Południowa Walia , w Australii .
Otwarcie stacji nastąpiło 19 maja 1869 roku .
Peron i usługi .
Stacja ma jeden działający peron i jeden zamknięty na południowym krańcu .
Muswelbrook jest obsługiwany przez pociągi NSW TrainLink linii Hunter kursujące między Hamilton ( Newcastle ) i Scone .
