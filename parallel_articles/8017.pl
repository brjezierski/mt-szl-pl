Musée d ’ Orsay
Muzeum Orsay ( ) – muzeum w Paryżu , położone na lewym brzegu Sekwany .
Zbiory zawierają głównie sztukę francuską z lat 1848–1918 : malarstwo , rzeźbę , fotografię oraz meble .
Dzieła te są młodsze od tych , które prezentowane są w Luwrze , a starsze od zbiorów Centre Georges Pompidou .
Rocznie muzeum odwiedza ok. 4 miliony zwiedzających .
Najbliższą stacją metra jest Solférino .
Historia .
W 1897 francuska `` Compagnie de chemins de fer '' zakupiła od państwa tereny pod budowę nowego dworca kolejowego , ponieważ dotychczas istniejący , Gare d ’ Austerlitz , znajdował się zbyt daleko od centrum miasta .
Rok później spomiędzy trzech architektów – Lucien Bénarda , Lucien Magne ’ a i Victora Lalouxa na architekta dworca d ’ Orsay wybrano tego ostatniego .
Nowy budynek musiał harmonizować z elegancką dzielnicą oraz sąsiedztwem Luwru i Legii Honorowej .
Zaprojektowany przez Lalouxa budynek miał eklektyczną fasadę i szklany dach oraz dwa ogromne zegary , zachowane do dziś ( jeden znajduje się w głównym holu , drugi w kawiarni `` Café des Hauteurs '' ) .
Uroczysta inauguracja budowli odbyła się 14 lipca 1900 roku , podczas wystawy światowej .
Przez następnych 39 lat budynek pełnił rolę dworca kolejowego , jednak z powodu elektryfikacji kolei długość pociągów znacznie się zwiększyła i nie mieściły się one już na dworcu d ’ Orsay .
Został więc zamknięty i od tego czasu zmieniało się jego przeznaczenie – mieścił się tam teatr Renault-Barrault , sale domu aukcyjnego Drouot , służył także jako tło w wielu filmach ( m.in .
w `` Procesie '' Orsona Wellesa ) .
Propozycja wyburzenia budynku napotkała na sprzeciw społeczeństwa , które nie chciało by dworzec podzielił los Les Halles .
W 1978 roku wpisano go na listę zabytków i tym samym uniemożliwiono jego wyburzenie .
Kilka lat wcześniej pojawił się też pomysł , by dawny dworzec przekształcić w muzeum sztuki XIX wieku .
Prace nad realizacją tej koncepcji rozpoczęły się w 1979 roku , kierowała nimi architekt Gae Aulenti .
Uroczystego otwarcia Musée d ’ Orsay dokonał prezydent François Mitterrand 1 grudnia 1986 roku .
Dla zwiedzających zostało ono udostępnione 9 grudnia 1986 roku .
Kolekcja .
Kolekcja muzeum składa się ze zbiorów malarstwa , rzeźby , sztuki użytkowej , fotografii i grafiki .
Istnieje także dział poświęcony architekturze , gdzie można znaleźć rysunki , projekty i modele budynków .
