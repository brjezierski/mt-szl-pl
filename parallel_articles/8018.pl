Mutare
Mutare ( do 1982 r. Umtali ) – miasto we wschodnim Zimbabwe będące ośrodkiem administracyjnym prowincji Manicaland , leżące przy linii kolejowej z Harare do Beiry w Mozambiku .
Liczy ok. 132 tys .
mieszkańców ( 1992 ) .
W Mutare znajduje się rafineria ropy naftowej ( ropa dostarczana jest rurociągiem z Beiry ) .
Przemysł spożywczy ( gł .
przetwórstwo kawy i herbaty ) , drzewny , materiałów budowlanych , chemiczny .
W mieście montowane są samochody oraz znajdują się zakłady naprawcze taboru kolejowego .
Węzeł drogowy , lotnisko , muzeum regionalne .
W pobliżu miasta wydobywa się boksyty .
Czwarte co do wielkości miasto kraju .
