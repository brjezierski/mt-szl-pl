Muzeum Miniaturowej Sztuki Profesjonalnej Henryk Jan Dominiak w Tychach
Muzeum Miniaturowej Sztuki Profesjonalnej Henryk Jan Dominiak w Tychach – muzeum sztuki nowoczesnej w Tychach założone w 2013 roku przez Henryka Jana Dominiaka .
Placówka mieści się przy ul .
Żwakowskiej 8/66 .
Muzeum podlega i jest nadzorowane przez Ministerstwo Kultury i Dziedzictwa Narodowego .
Działalność .
Muzeum zostało założone w 2013 roku w tyskiej dzielnicy Śródmieście , na parterze mieszkalnego wieżowca .
Do 2012 rezydowała w tym miejscu mobilna galeria sztuki współczesnej `` Po drodze '' .
Z uwagi na alkierz wystawowy o wymiarach 6,15 m² , jest określane w polskich publikacjach regionalnych jako najmniejsze muzeum Świata .
Skolekcjonowane eksponaty zostały pozyskane w formie darowizn .
Przyuliczne życie kulturalne .
11 sierpnia 2016 roku miasto podpisało umowę z Muzeum na użyczenie działki z przeznaczeniem na zieleń i aranżowanie spotkań kulturalnych przy ulicy Źwakowskiej – strefę oznaczono terminem `` Zielone Tychy Kultura '' .
Na skwerze zbudowano makietę miasta .
Po wytyczonych głównych arteriach miasta z kostki granitowej można spacerować .
Ponadto znajduje się tam pomnik – kamień pamiątkowy ku czci tragicznie zmarłych w katastrofie prezydenckiego samolotu Tu-154M pod Smoleńskiem .
Niesienie pomocy jej potrzebującej .
Muzeum jest dodatkowo zaangażowane w przedsięwzięcia charytatywne .
