Zbiory .
Od powstania muzeum ( 16 kwietnia 2013 ) , muzeum zebrało 970 eksponatów .
22 października 2013 zostały one podzielone na cztery sekcje ( nazwane nadzbiorami ) :
Nadzbiór obrazów , rysunków i grafik
Nadzbiór rzeźbiarstwa i ceramik
Nadzbiór falerystyki , weksylologii , heraldyki , symboliki i emblematyki
Nadzbiór broni białej
Wystawiennictwo .
