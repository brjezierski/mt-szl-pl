Mvezo
Mvezo – miejscowość w Południowej Afryce , w prowincji Przylądkowej Wschodniej .
Znajduje się w gminie King Sabata Dalindyebo w dystrykcie O.R .
Tambo .
Leży nad rzeką Mbhashe , około 45 km na południowy zachód od miasta Mthatha .
Mvezo zajmuje powierzchnię 2,13 km² .
Według spisu ludność przeprowadzonego w 2011 znajdowało się tu 178 gospodarstw domowych i zamieszkiwało 810 osób , spośród których wszyscy to czarni Afrykanie , a 98,27 % posługiwało się językiem xhosa .
Mvezo jest miejscem narodzin Nelsona Mandeli .
Znajduje się tu muzeum poświęcone życiu byłego prezydenta RPA i przywódcy ruchu przeciw apartheidowi .
