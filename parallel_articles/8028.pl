Grzybówka alkaliczna ( `` Mycena alcalina '' ( Fr . )
P .
Kumm . )
– gatunek grzybów należący do rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Po raz pierwszy takson ten zdiagnozował w roku 1818 Elias Fries nadając mu nazwę `` Agaricus alcalinus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1871 Paul Kummer .
