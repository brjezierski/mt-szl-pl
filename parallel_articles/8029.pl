Grzybówka czarnoniebieskawa
Grzybówka czarnoniebieskawa ( `` Mycena atrocyanea '' ( Batsch ) Gillet ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1786 r. August Johann Georg Karl Batsch , nadając mu nazwę `` Agaricus atrocyaneus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Claude-Casimir Gillet w 1876 r .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica1–4 cm z tępym garbem , prążkowany .
Powierzchnia brązowo-czarna lub niebiesko-czarna do ciemnoniebiesko-szarej , delikatnie i nietrwale oprószona .
Bladoniebiesko-czarne z lekkim połyskiem , drobne z międzyblaszkami lub rozwidlone .
