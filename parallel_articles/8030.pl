Grzybówka bulwiasta
Grzybówka bulwiasta ( `` Mycena bulbosa '' ( Cejp ) Kühner ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1930 r. Karel Cejp , nadając mu nazwę `` Apseudomycena bulbosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Robert Kühner w 1938 r .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 2–8 mm , pokryty dającą się oddzielić galaretowatą błonką ( niełatwą do zaobserwowania ) .
