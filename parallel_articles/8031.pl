Grzybówka równobarwna
Grzybówka równobarwna ( `` Mycena concolor '' ( J.E .
Lange ) Kühner ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1930 r. Jakob Emanuel Lange , nadając mu nazwę `` Omphalia picta '' var .
`` concolor '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Robert Kühner w 1938 r .
Nazwę polską nadała Maria Lisiewska w 1987 r .
