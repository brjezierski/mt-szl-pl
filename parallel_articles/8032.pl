Grzybówka żółtawa
Grzybówka żółtawa ( `` Mycena flavescens '' Velen . )
– gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w Josef Velenovský 1920 r .. Synonim : `` Mycena luteoalba '' var .
`` sulphureomarginata '' .
nazwę polską nadała mu Maria Lisiewska w 1987 r .
Morfologia .
Średnica 5–30 mm , początkowo wąskostożkowaty , potem szerokostożkowaty , paraboliczny , wypukły , w końcu prawie płaski , czasem z małym garbkiem , półprzezroczyście prążkowany .
Powierzchnia naga , o barwie od kości słoniowej do żółtawobiałej , czasem bardziej żółtawej w środku .
W liczbie 20–24 dochodzących do trzonu , wąsko przyrośnięte , zbiegające z krótkim ząbkiem , lub bez , białe .
