Grzybówka szarobrązowa
Grzybówka szarobrązowa ( `` Mycena latifolia '' ( Peck ) A.H .
Sm . )
– gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w Charles Horton Peck 1872 r. nadając mu nazwę `` Agaricus latifolius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Alexander Hanchett Smith w 1935 r ..
Synonimy :
Nazwę polską nadała mu Maria Lisiewska w 1987 r ..
Morfologia .
Średnica 5–25 mm , początkowo paraboliczny lub stożkowy do wypukłego , potem spłaszczony , często wklęsły na środku lub z niewielkim garbem , półprzezroczyście prążkowany .
