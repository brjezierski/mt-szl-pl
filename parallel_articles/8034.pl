Grzybówka pofałdowana
Grzybówka pofałdowana ( `` Mycena mucor '' ( Batsch ) Quél . )
– gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w 1786 r. August Johann Georg Karl Batsch nadając mu nazwę `` Agaricus mucor '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Lucien Quélet w 1875 r .
Synonimy :
Polską nazwę nadała Maria Lisiewska w 1987 r ..
