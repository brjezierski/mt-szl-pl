Grzybówka fioletowoszara
Grzybówka fioletowoszara , grzybówka paraboliczna ( `` Mycena parabolica '' ( Fr . )
Quél . )
– gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w 1838 r. Elias Fries nadając mu nazwę `` Agaricus parabolicus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Lucien Quélet w 1872 r .
Synonimy : :
Maria Lisiewska w 1987 r. nadała mu polską nazwę grzybówka paraboliczna .
Władysław Wojewoda w 2003 r. zaproponował nazwę grzybówka fioletowoszara .
Występowanie i siedlisko .
Występuje w Ameryce Północnej i niektórych krajach Europy .
Władysław Wojewoda w zestawieniu grzybów wielkoowocnikowych Polski w 2003 r. przytacza 10 stanowisk z uwagą , że rozprzestrzenienie i stopień zagrożenia tego gatunku nie są znane .
Grzyb saprotroficzny .
Występuje w lasach liściastych i mieszanych na opadłych gałęziach i pniach drzew , zwłaszcza buka i jodły .
