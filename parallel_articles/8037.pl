Grzybówka bladoszara
Grzybówka bladoszara ( `` Mycena smithiana '' Kühner ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w 1938 r. Robert Kühner na liściach dębu we Francji .
Polską nazwę nadała Maria Lisiewska w 1987 r .
Morfologia .
Średnica 1–5 mm , początkowo półkulisty lub paraboliczny , stożkowaty , potem płasko wypukły , często z nieco spłaszczonym środkiem lub małym garbkiem , półprzeźroczyście prążkowany .
Powierzchnia oprószona , błyszcząca , jasnoróżowa lub różowawo-morelowa do brązowawo-różowej , często ciemniejsza w środku , z wiekiem często blednąca do białawo różowej .
