Grzybówka nadwodna
Grzybówka nadwodna ( `` Mycena tubarioides `` ( Maire ) Kühner ) – gatunek grzybów z rodziny grzybówkowatych ( `` Mycenaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycena , Mycenaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w 1830 r. René Charles Maire , nadając mu nazwę `` Omphalia tubarioide '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu Robert Kühner w 1838 r .
Polską nazwę nadała Maria Lisiewska w 1987 r .
Morfologia .
Średnica 2–8 mm , początkowo półkulisty , potem paraboliczny , szeroko dzwonkowaty , w końcu prawie płaski , a nawet nieco wklęsły lub z małym garbkiem .
