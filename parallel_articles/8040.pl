Czapka
Czapka – nakrycie głowy , wykonane przeważnie z miękkiego materiału ( niekiedy z twardymi wstawkami jak denko lub daszek ) , którego głównym zadaniem jest ochrona głowy przed niekorzystnymi warunkami atmosferycznymi : zimnem , gorącem , deszczem , śniegiem , wiatrem , promieniami słonecznymi itp .
Na kształt , fason i kolor czapki istotny wpływ ma moda panująca w danym czasie i miejscu .
Niektóre rodzaje czapek noszone są ze względów religijnych ( np .
piuska , jarmułka ) , inne jedynie przy okazjach ceremonialnych ( np .
infuła ) .
Czapki wojskowe i służb mundurowych mają ściśle określony krój i kolor i mogą być noszone tylko przez uprawnionych przedstawicieli tych służb ( przy tym wyróżnia się czapki galowe , używane tylko w przypadku uroczystości i ceremonii , oraz czapki polowe – noszone na co dzień i podczas ćwiczeń ) .
W Polsce cechy czapnicze istniały od XIV wieku .
W przeglądzie historycznym wyróżnić można następujące rodzaje i typy czapek :
Inne znaczenia .
Nazwą „ czapka ” ( lub „ czapa ” ) określa się także grubszą pokrywę ( np .
lodową lub śniegową ) okrywającą jakieś przestrzenie , rzeczy lub przedmioty .
W teorii organizacji termin „ czapka ” ( bądź „ czapa ” ) może także oznaczać jakieś władze nadrzędne ( zwykle w odcieniu pejoratywnym ) .
