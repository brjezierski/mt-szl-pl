Uwagi taksonomiczne : w publikacjach fitopatologicznych konidialna postać `` Mycosphaerella mori '' była różnie określana lub błędnie opisywana jako jeden z gatunków `` Cercospora '' , `` Cercosporella '' , `` Cylindrosporium '' , `` Phloeospora '' , `` Septoria '' i `` Septogloeum '' , w wyniku czego informacje na temat patologii udokumentowano pod różnymi nazwami .
Morfologia i rozwój .
Grzybnia rozwija się w tkance miękiszowej żywych liści .
W miejscach jej rozwoju powstają plamy .
W ich obrębie tworzą się pod naskórkiem oddzielne , lub zlewające się z sobą acerwulusy o rozmiarach 110–200 ( –300 ) μm , zbudowane z nieregularnych komórek pseudoparenchymy .
Komórki konidiotwórcze cylindryczne , przeźroczyste i bezbarwne ( hialinowe ) , holoblastyczne , czasami również sympodialne .
