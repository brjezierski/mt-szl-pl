Mycosphaerella mori
Mycosphaerella mori ( Fuckel ) F.A .
Wolf – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt obligatoryjny i saprotrof rozwijający się na liściach morwy ( `` Morus '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycosphaerella , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1870 r. Leopold Fuckel nadając mu nazwę `` Sphaerella mori '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Frederick Adolph Wolf w 1935 r .
