Mycosphaerella pomi
Mycosphaerella pomi ( Pass . )
Lindau – gatunek grzybów z klasy `` Dothideomycetes '' .
U jabłoni ( `` Malus '' ) wywołuje chorobę o nazwie drobna plamistość liści jabłoni .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycosphaerella , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1876 r. Giovanni Passerini nadając mu nazwę `` Sphaerella pomi '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Gustav Lindau w 1897 r .
Synonimy :
Morfologia i rozwój .
Pasożyt obligatoryjny i saprotrof .
Jego grzybnia rozwija się w tkance miękiszowej żywych liści , a później także martwych , opadłych liści .
Wytwarza na obydwu ich stronach , ale głównie na górnej rozproszone pyknidia o rozmiarach 80–120 μm .
Powstają w nich jednokomórkowe , owalne konidia o rozmiarach 2–3 μm × 5–8 μm .
Wiosną powodują one infekcje pierwotne .
W okresie wegetacyjnym na żywych liściach wytwarzane są konidia , które dokonują infekcji wtórnych rozprzestrzeniając chorobę .
Odbywa się to w okresach dłuższej , deszczowej pogody .
W 1890 r. w Japonii po raz pierwszy zaobserwowano występowanie tego patogenu na owocach gruszy domowej ( `` Pyrus communis '' ) .
