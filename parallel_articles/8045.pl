Mycosphaerella pyri
Mycosphaerella pyri ( Auersw . )
Boerema – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny u gruszy wywołujący chorobę o nazwie biała plamistość liści gruszy lub septorioza gruszy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycosphaerella , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy anamorfę zdiagnozował w 1850 r. John Baptiste Desmazières nadając jej nazwę `` Septoria pyricola '' .
W 1869 r. B. Auerswald takson ten opisał jako `` Sphaerella pyri '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Gerhard H. Boerema w 1970 r .
Synonimy :
Morfologia i rozwój .
Plamistość liści .
