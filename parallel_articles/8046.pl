Mycosphaerella rubi
Mycosphaerella rubi Roark – gatunek grzybów z klasy `` Dothideomycetes '' .
U malin i jeżyn wywołuje chorobę o nazwie biała plamistość liści maliny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycosphaerella , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonim : `` Septoria rubi '' Berk .
& amp ; M.A .
Curtis 1874 .
Morfologia i rozwój .
Pasożyt obligatoryjny i saprotrof .
Jego grzybnia rozwija się w tkance miękiszowej żywych liści i wytwarza na obydwu ich stronach , ale głównie na górnej rozproszone , bardzo drobne pyknidia o barwie od rdzawej do brązowej i rozmiarach 45–80 μm .
Mają okrągłe , nieco kanciaste ostiole o szerokości 25–40 μm , otoczone ciemniejszymi komórkami .
Powstają w nich nitkowate konidia o zmiennym kształcie , zazwyczaj sierpowate , czasami lekko faliste .
Mają rozmiar 30–65 × 1,5–2 ( 3 ) μm i posiadają mniej lub bardziej wyraźne przegrody
Na porażonych przez tego patogena liściach malin i jeżyn pojawiają się drobne , białe plamki , otoczone rdzawo-brunatnymi otoczkami .
Po pewnym czasie porażona przez grzybnię tkanka liścia wykrusza się i w środku plamy powstaje dziurka .
