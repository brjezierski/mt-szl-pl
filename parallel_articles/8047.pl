Mycosphaerella ulmi
Mycosphaerella ulmi Kleb .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny rozwijający się na liściach wiązów ( `` Ulmus '' ) .
Występuje w USA , Europie , Australii i Nowej Zelandii .
W Polsce podano jego występowanie na wszystkich dziko rosnących gatunkach wiązów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Mycosphaerella , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Charakterystyka .
Na liściach wiązu występuje wiele pasożytniczych grzybów , które rozpoznać można tylko badaniem mikroskopowym .
`` Mycosphaerella ulmi '' należy do częściej występujących .
