Myitkyina
Myitkyina – miasto w północnej Mjanmie , nad rzeką Irawadi , stolica stanu Kaczin .
W 2011 miasto liczyło 62 936 mieszkańców ; dla porównania , w 1983 było ich 56 427 , a w 1960 – około 20 tys .
Miasto leży nad rzeką Irawadi .
Rozwinięty przemysł drzewny i spożywczy ( łuszczarnie ryżu ) .
Ponadto w mieście znajduje się port lotniczy Myitkyina .
