Historia .
Miasto założono w 1570 .
Prawa miejskie od roku 1578 ( ponownie od 1940 ) .
Było własnością Mikołaja Tarły - chorążego przemyskiego , dworzanina i sekretarza królewskiego , który dokonał lokacji miasta na prawie magdeburskim .
W okresie międzywojennym miasto znajdowało się w powiecie żydaczowskim ( województwo stanisławowskie ) w Polsce .
W 1931 r. miasto liczyło 632 budynków ( zagród ) i 3625 mieszkańców .
W latach 1943 - 1944 z rąk nacjonalistów ukraińskich z UPA śmierć poniosło 18 Polaków , w tym Kazimierz Podgórski ps .
`` Zych '' , dowódca plutonu Armii Krajowej .
Kościół parafialny w Mikołajowie .
Wzniesiony w latach 1633-34 z fundacji starosty drohowyskiego Mikołaja Ostroroga , w miejsce kościoła ufundowanego przez Jerzego Mniszcha i zburzonego przez Tatarów w 1620 r. Murowany , jednonawowy , tynkowany , konsekrowany w 1636 r. Wewnątrz znajdował się łaskami słynący obraz Matki Bożej , ufundowany przez Marię Eleonorę z Kunickich Rzewuską .
W latach 70 .
XIX wieku na fasadzie kościoła zainstalowano duży zegar .
Po II wojnie światowej świątynia zamieniona była w salę gimnastyczną , a następnie w płac ślubów .
W 1990 r. została odzyskana przez wiernych .
