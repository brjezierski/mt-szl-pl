Mikulińce
Mikulińce ( ukr .
Мику́линці / `` Mykułynci '' ) – osiedle typu miejskiego w rejonie trembowelskim obwodu tarnopolskiego , 18 km na południe od Tarnopola , nad rzeką Seret na Podolu na Ukrainie .
Mikulińcom administracyjnie podlegają także : Wola ( do 1939 r. Wola Mazowiecka ) , Konopkówka i Krzywki ( odpowiednio ukr .
`` Воля , Конопківка , Кривки '' ) .
W okolicy znajduje się stacja kolejowa Mikulińce-Strusów , na linii z Tarnopola do Kopyczyniec .
Historia .
Gród Mikulin wymieniany był w źródłach już w 1084 roku .
Po najazdach tatarskich w połowie XIII w. osada upadła , a na jej miejscu powstała wieś Mikulińce .
Właścicielem miasteczka był Krzysztof Strzemeski .
Aleksander Walewski , miecznik sieradzki , 1 maja 1723 sprzedał wielu posiadłości , m.in .
klucz mikuliniecki , ks .
