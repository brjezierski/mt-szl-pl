Mirt
Mirt ( `` Myrtus '' L. ) – rodzaj roślin z rodziny mirtowatych .
Obejmuje tylko dwa gatunki występujące w basenie Morza Śródziemnego ( na wschodzie sięgające po Pakistan , na południu po Etiopię ) , dawniej zaliczano tu wiele gatunków tworzących współcześnie osobne rodzaje , występujące m.in .
w Ameryce Południowej i Nowej Zelandii .
Rośliny te rosną w naturze w suchych formacjach zaroślowych .
W starożytności mirt był symbolem piękna i młodości , używany był w rytuałach świątecznych i ceremoniach , wieńce z jego gałązek zakładano na głowy zwycięzcom igrzysk olimpijskich , zdobiły głowy sędziów i składano je do rzymskich grobów .
Gałązki mirtu tradycyjnie wykorzystywane są przy tworzeniu bukietów ślubnych .
Wonne , bogate w olejki kwiaty i owoce służą do wyrobu perfum , wykorzystywane są także leczniczo .
Drewno służy do wyrobu lasek i mebli , kora i korzenie są źródłem tanin używanych do garbowania skóry .
Mirt zwyczajny uprawiany jest w kilku odmianach jako ozdobny .
Systematyka .
Rodzaj z podrodziny `` Myrtoideae '' , rodziny mirtowatych ( `` Myrtaceae '' ) .
Należy do plemienia `` Myrteae '' ( liczącego 49 rodzajów i ok. 2,5 tys .
gatunków ) , w którego obrębie zajmuje pozycję bazalną .
