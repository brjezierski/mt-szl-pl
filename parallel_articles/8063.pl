Mysłowice
Mysłowice ( , ) – miasto na prawach powiatu w Polsce , na historycznym Górnym Śląsku , w historycznej ziemi pszczyńskiej , w województwie śląskim .
Według danych GUS z 31 grudnia 2021 r. , Mysłowice liczyły 72 124 mieszkańców i były pod względem liczby ludności piętnastym miastem w województwie śląskim , a także 47. spośród najludniejszych miast w Polsce .
Położenie .
Mysłowice położone są na Wyżynie Śląskiej , stanowiącej część Wyżyny Śląsko-Krakowskiej , nad rzeką Przemszą i Brynicą .
Miasto leży w obrębie Wyżyny Katowickiej .
Mysłowice leżą w centrum Górnośląskiego Okręgu Przemysłowego ( GOP ) i są jednym z ośrodków centralnych konurbacji górnośląskiej .
Przez miasto przepływa Czarna Przemsza , która w pobliżu Mysłowic łączy się z Białą Przemszą , tworząc Przemszę .
Przez miasto przepływa także Rawa , Brynica , potok Bolina , Przyrwa ( Ławecki Potok ) oraz Rów Kosztowski .
Mysłowice graniczą od zachodu z Katowicami , od północy z Sosnowcem , od wschodu z Jaworznem , a od południa z Imielinem oraz Lędzinami .
