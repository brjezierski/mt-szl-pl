Nazwa .
Pochodzenie nazwy miejscowości nie jest jednoznaczne i równolegle funkcjonuje na ten temat kilka teorii .
Nazwa miejscowa `` Mysłowice '' pochodzi od starosłowiańskiego imienia `` Mysław '' , `` Mysł '' lub `` Miłosław '' .
Końcówka „ ice ” lub jej staropolska starsza wersja ( „ icze ” ) charakterystyczna jest dla słowiańskich nazw patronimicznych wywodzących się od osad rodowych .
Oznacza ona potomków założyciela lub właściciela miasta , czyli „ Mysłowiczów ” .
Założyciel miejscowości rycerz Mysław miał stanowić wzór dla wizerunku męskiej głowy z herbu Mysłowic .
Jacob Lustig – autor pierwszej monografii na temat miasta pisze , że nazwa pochodzi od polskiej nazwy procesu poznawczego `` „ myślenia ” '' – `` „ Der name Myslowitz kommt jedenfalls von dem polnischen Worte „ myśleć ” ( ... ) ” '' .
Na poparcie tego przytacza legendę o początkach miejscowości według , której Mysłowice pierwotnie leżały w lesie z dala od wody i z powodu tej lokalizacji mieszkańcy cierpieli na jej niedostatki .
Po namyśle przenieśli się nad rzekę , gdzie na swoje potrzeby wody mieli pod dostatkiem .
