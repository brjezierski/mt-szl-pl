Myszowice
Myszowice ( niem .
`` Mauschwitz '' , 1936–1945 `` Mauschdorf '' ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
Nazwa pochodzi od polskiej nazwy zwierzęcia z rodziny gryzoni - myszy .
W kronice łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej w latach 1295-1305 miejscowość wymieniona jest w formie `` Myssowitz '' .
Miejscowość nosiła również nazwę niemiecką `` Mauschwitz '' .
Historia .
Myszowice wzmiankowane są w dokumentach już w 1300 roku i jako osada lokowane były na prawie niemieckim w XIII wieku .
Początkowo wieś należała do dóbr pańskich Korfantów .
