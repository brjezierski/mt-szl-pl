Myszuryn Rih
Myszuryn Rih ( ukr .
Мишурин Ріг ) – wieś na Ukrainie w rejonie wierchniednieprowskim obwodu dniepropetrowskiego , centrum myszurynriskiej rady wiejskiej .
Niegdyś położona nad Dnieprem forteca i osada kozacka , obecnie duża wieś zamieszkana przez 1 945 mieszkańców ( 2001 ) , posiadająca przedszkole , liceum ogólnokształcące , bibliotekę , dom kultury , ambulatorium i punkt felczersko-akuszerski .
Położenie .
Wieś położona na prawym brzegu Zbiornika Kamieńskiego w północno-zachodniej części obwodu dniepropetrowskiego w pobliżu granicy z obwodem kirowohradzkim .
Najbliższa zamieszkana miejscowość - Mosty - znajduje się 6 km na południe .
Historia .
W okolicy miejscowości znaleziono zabytki archeologiczne z epoki miedzi i brązu , ślady pobytu koczowniczych Scytów ( VII-II wiek p.n.e .
