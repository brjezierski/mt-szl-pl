Myślibórz ( stacja kolejowa )
Myślibórz – zlikwidowana stacja kolejowa w Myśliborzu , w województwie zachodniopomorskim , w Polsce .
Funkcjonowała 118 lat , od 31 sierpnia 1882 r. do 2 kwietnia 2000 r. , kiedy to przejechał ostatni pociąg pasażerski na trasie Myślibórz – Głazów .
Obsługiwała linię Kostrzyn nad Odrą — Stargard , z odnogą do Gorzowa Wielkopolskiego .
W kolejnych latach budynki stacyjne ulegają dewastacji .
W 2011 roku w budynku dworca mieścił się sklep meblowy .
