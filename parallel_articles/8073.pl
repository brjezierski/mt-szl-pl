1 stycznia 1969 do gromady Myślenice przyłączono obszar zniesionej gromady Bysina .
W kwietniu 1969 dla gromady ustalono 27 członków gromadzkiej rady narodowej .
1 stycznia 1970 gromada składała się ze wsi : Borzęta , Bysina , Jawornik , Jasienica , Polanka i Zawada .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 reaktywowano zniesioną w 1954 roku gminę Myślenice .
