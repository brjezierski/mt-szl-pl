Mátészalka
Mátészalka – miasto powiatowe w komitacie Szabolcs-Szatmár-Bereg w północno-wschodnich Węgrzech .
Położenie .
Mátészalka leży kilka kilometrów na zachód od rzeki Kraszna , na granicy krainy Nyírség z Równiną Satmarsko-Berehowską .
Stanowi węzeł komunikacyjny .
Przez miasto przebiega droga 49 , biegnąca od drogi 41 do przejścia granicznego Csengersima - Petea na granicy z Rumunią , kończy się tu również droga 471 z Debreczyna .
