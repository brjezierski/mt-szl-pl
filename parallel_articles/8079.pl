Mérida ( stan )
Mérida ( hiszp .
: `` Estado Mérida '' ) – stan w Wenezueli ze stolicą w mieście Mérida .
Stan zajmuje powierzchnię 11 300 km² , a w roku 2011 zamieszkiwały go 828 592 osoby .
Dla porównania , w 1970 było ich 335,4 tys .
Powierzchnia stanu jest górzysta , przebiega przez niego pasmo Cordillera de Mérida .
Na terenie stanu wznosi się 76 szczytów o wysokości powyżej 4000 m. W północno-zachodniej części małe niziny .
Główną rzeką stanu jest Chama , nad którą leży stolica stanu .
Stan jest miejscem uprawy kawy , pszenicy , bananów , ziemniaków oraz hodowli bydła , koni , owiec .
Rozwinięty przemysł włókienniczy i drzewny .
Przez stan przebiega odgałęzienie Autostrady Panamerykańskiej .
