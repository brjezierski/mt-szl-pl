Mönchengladbach
Mönchengladbach ( do 1960 `` München-Gladbach '' , `` M. Gladbach '' ) – miasto na prawach powiatu w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf .
Leży 70 m n.p.m .
Gospodarka .
Przemysł tekstylny dominował przez wiele lat gospodarkę w mieście .
W dzisiejszych czasach istnieją przede wszystkim przedsiębiorstwa zajmujące się marketingiem wyrobów tekstylnych .
Drugą gałęzią gospodarki jest budowa maszyn ( przede wszystkim tekstylnych ) i przemysł elektrotechniczny .
Sport .
Borussia Mönchengladbach – klub piłkarski założony w 1900 roku , 5-krotny Mistrz Niemiec , 3-krotny zdobywca Pucharu Niemiec oraz 2-krotny Zwycięzca Pucharu UEFA .
Mönchengladbach był organizatorem Mistrzostw Świata w hokeju na trawie w 2006 r .
