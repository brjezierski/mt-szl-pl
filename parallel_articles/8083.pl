Mühlhausen/Thüringen
Mühlhausen/Thüringen – miasto powiatowe w Niemczech , w kraju związkowym Turyngia , siedziba powiatu Unstrut-Hainich .
Liczy 36 200 mieszkańców ( 31 grudnia 2018 ) .
Historia .
Miejscowość po raz pierwszy wzmiankowana w 967 .
Od 1251 wolne miasto , w 1286 przystąpiło do Hanzy , uchodziło za jedno z najpotężniejszych miast na obszarze dzisiejszej Turyngii .
