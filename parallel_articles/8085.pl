Monachium
Monachium ( niem .
`` München '' , ; baw .
`` Minga '' ) – miasto na prawach powiatu w południowych Niemczech , stolica kraju związkowego Bawaria , siedziba rejencji Górna Bawaria , regionu Monachium , powiatu Landkreis München ( do którego jednak miasto nie należy ) oraz Stowarzyszenia Krajowego ( `` Landesverband '' ) Technisches Hilfswerk , trzecie pod względem liczby ludności miasto Niemiec , zamieszkiwane pod koniec 2019 r. przez niemal 1,5 mln osób .
Monachium położone jest na Wyżynie Bawarskiej , nad rzeką Izarą .
Jest siedzibą Federalnego Trybunału Finansowego ( `` Bundesfinanzhof '' ) oraz centrali prestiżowego Towarzystwa Maxa Plancka prowadzącego działalność badawczo-rozwojową w Niemczech i innych krajach Europy .
Miasto jest ponadto głównym ośrodkiem gospodarczym i kulturalno-naukowym południowej części kraju .
Historia .
W 1158 r. Henryk Lew , książę Bawarii , dokonał lokacji miasta , nadając jednocześnie benedyktynom przywilej organizowania targów .
Miasto powstało przy zamku i klasztorze , stąd nazwa `` bei den Mönchen '' – przy mnichach .
Dogodna lokalizacja na szlaku handlu solą i most na Izarze zadecydowały o szybkim rozwoju miasta .
Od połowy XIII wieku Monachium stało się siedzibą rodu von Wittelsbach , który w 1255 przeniósł tu swoją rezydencję książęcą i władał miastem przez następne stulecia .
W 1327 i 1429 miały miejsce wielkie pożary miasta .
W latach 1468–1488 wzniesiono główną świątynię miasta – Katedrę Najświętszej Marii Panny .
W okresie wojny trzydziestoletniej , w 1632 , miasto zajęte zostało przez Szwedów pod wodzą Gustawa Adolfa .
W 1759 została założona Bawarska Akademia Nauk , a w 1770 szkoła będąca zalążkiem Akademii Sztuk Pięknych .
Od XIX wieku .
W 1806 r. , podczas wojen napoleońskich , miasto zostało stolicą nowo utworzonego Królestwa Bawarii .
W XIX wieku rozpoczął się okres przyspieszonego rozwoju Monachium .
Maksymilian I – pierwszy król – zdobył środki na wzniesienie klasycznych budowli .
Został zbudowany Teatr Narodowy i Odeonsplatz .
Syn i następca Maksymiliana I – król Ludwik I , miłośnik kultury antycznej i włoskiego renesansu – sprowadził architektów , rzeźbiarzy i malarzy .
Zlecił budowę nowej siedziby dla uniwersytetu z Landshut , Starej Pinakoteki , Gliptoteki , monumentalnej bramy-pomnika Propylejów , Dzielnicy Muzeów ( `` Kunstareal '' ) , Portyku Marszałków ( `` Feldherrnhalle '' ) , `` Siegestor '' , `` Königsplatz '' , `` Ludwigstraße '' ( reprezentacyjnej ulicy ) , `` Ruhmeshalle '' i gigantycznego posągu Bavarii .
Monachium stało się jednym z najważniejszych ośrodków kultury europejskiej , tworzył tu m.in .
kompozytor Richard Wagner .
W 1852 został ukończony łuk triumfalny Brama Zwycięstwa ( `` Siegestor '' ) , w 1874 monumentalny gmach Maximilianeum , w 1909 monumentalny Nowy Ratusz .
W 1919 powstała w Monachium Bawarska Republika Rad .
W okresie Republiki Weimarskiej miasto stanowiło ośrodek ruchu nazistowskiego ( pucz monachijski ) .
W 1938 było miejscem zawarcia układu między Niemcami , Włochami , Francją a Wielką Brytanią .
Miasto zostało poważnie zniszczone w czasie II wojny światowej .
