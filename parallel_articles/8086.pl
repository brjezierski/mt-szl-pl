Münnerstadt
Münnerstadt – miasto w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Frankonia , w regionie Main-Rhön , w powiecie Bad Kissingen .
Leży w Grabfeldzie , ok. 10 km na północny wschód od Bad Kissingen , nad rzeką Lauer , przy drodze B19 i linii kolejowej Eisenach - Schweinfurt .
