Münsing
Münsing – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , w powiecie Bad Tölz-Wolfratshausen .
Leży około 25 km na północny zachód od Bad Tölz , nad jeziorem Starnberger See , przy autostradzie A95 .
Polityka .
Wójtem gminy jest Michael Grasl , rada gminy składa się z 16 osób .
