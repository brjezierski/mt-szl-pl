Münster ( Bawaria )
Münster – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Szwabia , w regionie Augsburg , w powiecie Donau-Ries , wchodzi w skład wspólnoty administracyjnej Rain .
Leży około 15 km na południowy wschód od Donauwörth , nad Lech i Friedberger Ach .
1 lipca 2014 do gminy przyłączono 1,55 km2 pochodzącego z rozwiązanego obszaru wolnego administracyjnie Brand .
Polityka .
Rada gminy składa się z dwunastu osób .
Wójtem gminy jest Gerhard Pfitzmaier ; poprzednio urząd ten piastował Alois Stuber .
Oświata .
W gminie znajduje się przedszkole ( 50 miejsc ) .
