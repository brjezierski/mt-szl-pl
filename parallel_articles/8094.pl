Młynarska Wola
Młynarska Wola ( niem .
`` Herrndorf '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie elbląskim , w gminie Młynary .
Wieś lokowana w XIV w. Zachowały się ruiny gotyckiego kościoła z ok. 1300 r. , przebudowanego w wieku XVI .
Pierwszy kościół w Młynarskiej Woli pod wezwaniem św .
Tomasza wzniesiono ok. 1330 r. Zniszczony na przełomie XV–XVI w. , odbudowany w 1593 r. Nawa główna kościoła o rozmiarach 31 na 15 m. Kościół nieco rozbudowano w 1709 r. , a wcześniej w 1705 r. wzniesiono drewnianą wieżę .
Wieżę tę odrestaurowano w 1823 r. ( na chorągiewce wiatrowej znajdował się herb Dohnów ze Słobit ) .
Dzwony i zegar z wieży przeniesiono po II wojnie światowej do kościoła w Młynarach , natomiast barokowy ołtarz do Olsztyna .
Wspomniani Dohnowie ze Słobit m.in .
ufundowali wyposażenie barokowe kościoła w XVIII w. Ostatnim pastorem ewangelickim parafii Słobity–Młynarska Wola ( 1928–1945 ) był Johann Neumann , po wojnie pastor w Ilmenau-Rhoda ( Turyngia ) .
Po 1945 r. kościół przez jakiś czas służył za spichlerz Armii Czerwonej .
Później został przejęty przez prawosławnych .
Przez kilka lat nabożeństwa celebrował tam ks .
Naumow z Gdańska .
Z czasem kościół uległ zniszczeniu .
W latach 60. i 70. w kościele mieścił się magazyn GS , w którym prawdopodobnie składowano m.in .
nawozy sztuczne .
Jeszcze ok. 1990 r. istniał dach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa elbląskiego .
