Monako jako kraj niezrzeszony w Unii Europejskiej , lecz mający szczególne stosunki z Francją ma prawo do stowarzyszenia z krajami wchodzącymi w skład UE oraz ma możliwość wprowadzenia euro jako waluty narodowej .
Monako jest pełnoprawnym członkiem ONZ , do której państwo wstąpiło w 1993 roku .
4 października 2004 roku dołączyło do Rady Europy .
Monako jest również członkiem Interpolu oraz Międzynarodowej Organizacji Hydrograficznej , której siedziba znajduje się właśnie w Monako .
Monako ma 10 misji dyplomatycznych w Europie Zachodniej , ma stałego członka w Radzie Europy oraz ONZ .
Państwo ma także 106 generalnych konsulatów w 45 państwach świata .
76 krajów ma swoje ambasady lub konsulaty na terenie Monako .
Demografia .
Spośród ponad 37 tysięcy mieszkańców jedynie 9160 ( 24,3 % ) stanowią Monegaskowie ( obywatele księstwa ) ; pozostali to migranci , głównie z Francji i Włoch .
Jedynie 63,5 % obywateli kraju urodziło się na jego terenie , 0,9 % urodziło się poza jego granicami , 35,6 % zostało naturalizowanych ( w tym 19,1 % na drodze małżeństwa z osobą posiadającą obywatelstwo Monako ) .
Na terenie księstwa jest zatrudnionych blisko 51 tysięcy ludzi , więcej niż jego cała populacja .
W sektorze prywatnym ponad 3/4 zatrudnionych ( około 35 tys . )
dojeżdża z terytorium Francji .
W usługach pracuje 75 % ludności zawodowo czynnej , w przemyśle – 25 % .
Cała ludność mieszka w bardzo gęsto zaludnionym mieście – ok. 18 775 osób na km² .
Jest to największa gęstość zaludnienia w Europie i druga na świecie ( po Makau ) .
Wraz z sąsiadującymi miejscowościami po stronie francuskiej w zespole miejskim Monako-Mentona mieszka około 100 tysięcy ludzi .
