Gospodarka .
Produkt krajowy brutto na 1 mieszkańca Monako wynosi ok. 70 tysięcy USD rocznie .
Podstawą gospodarki są usługi , zwłaszcza bankowość , turystyka i handel nieruchomościami .
Przemysł Monako opiera się na produkcji żywności , przyrządów precyzyjnych , wyrobów farmaceutycznych , sprzedaży znaczków pocztowych i turystyce .
Port jachtowy i kasyno w Monte Carlo są słynne na całym świecie .
Monte Carlo jest ponadto ośrodkiem badań oceanograficznych .
Monako jest siedzibą wielu firm zagranicznych i banków ( z uwagi na korzystne przepisy podatkowe ) .
Istnieje tu rozbudowana sieć hoteli .
W 2000 w hotelach Monako nocowało 240 tysięcy turystów zagranicznych .
Znaczne dochody przynosi kasyno gry w Monte Carlo oraz emisja znaczków pocztowych i pamiątkarstwo .
W dzielnicy Fontvieille znajdują się nowoczesne zakłady przemysłu precyzyjnego , chemicznego ( w tym farmaceutycznego ) , odzieżowego i jubilerskiego , ponadto funkcjonuje lądowisko dla śmigłowców , nowoczesny stadion , przystań jachtowa .
Energia elektryczna w całości importowana jest z Francji .
W pobliżu terytorium Monako przechodzą : linia kolejowa i autostrada Nicea – Genua .
Transport wewnętrzny zapewniają samochody prywatne ( 676 samochodów osobowych na 1000 mieszkańców ) .
Monako łączy unia celna z Francją , która pośredniczy w całości wymiany handlowej księstwa .
Monako uczestniczy w Europejskiej Unii Gospodarczej i Walutowej .
