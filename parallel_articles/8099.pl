Od 1866 jest w unii celnej z Francją , a od 1918 Francja uzyskała prawo do stacjonowania wojsk na terenie Monako i zatwierdzania zawieranych przez to państwo umów międzynarodowych .
Z tych powodów ( szczególna więź z Republiką Francuską , która ogranicza jego suwerenność – podstawowy atrybut państwa ) w prawie międzynarodowym Monako określa się raczej jako niesuwerenną organizację terytorialną niż państwo .
W Monako odbywa się znany doroczny wyścig samochodowy Grand Prix Monako Formuły 1 , jeden z oryginalnych wyścigów Formuły 1 .
Na terenie księstwa jest także klub piłkarski AS Monaco FC grający w lidze francuskiej .
Monako jest chętnie odwiedzane przez turystów zwiedzających Lazurowe Wybrzeże .
Wśród głównych atrakcji tego miasta-państwa najważniejsze są : Pałac Książęcy , stare miasto , katedra św .
Mikołaja , Place du Casino wraz z kasynem , opera w Monte Carlo , ogrody tropikalne , ogrody japońskie i plaża Lavrotto .
Geografia .
Monako jest najmniejszym po Watykanie państwem świata .
Monako to państwo-miasto w południowej Europie , nad Morzem Śródziemnym , na Wybrzeżu Lazurowym .
Leży 18 km na wschód od francuskiej Nicei .
Klimat .
Monako , jak i całe Lazurowe Wybrzeże leży w strefie klimatu podzwrotnikowego o łagodnej śródziemnomorskiej odmianie .
Zimy w księstwie są wilgotne i łagodne , zaś lata suche i ciepłe .
Średnia temperatura zimą wynosi 8 °C , zaś latem od 23 do 26 °C .
Średnia suma opadów wynosi od 600 do maksymalnie 800 mm , opady występują głównie jesienią i zimą .
Latem deszcz prawie nie pada , a klimat kształtowany jest w głównej mierze przez masy powietrza zwrotnikowego .
Przyroda .
Monako stanowi przykład państwa , którego całe terytorium pokrywa infrastruktura miejska .
To oznacza , że naturalna roślinność praktycznie nie istnieje , zajmując jedynie niewielkie skrawki w postaci roślinności twardolistnej , która stanowiła naturalną formację roślinną tego regionu .
Powszechnie występują tu sadzone przez człowieka różne gatunki palm .
Na skalistym klifie w zachodniej części księstwa znajduje się Jardin exotique de Monaco .
Do zwierząt lądowych dziko żyjących w Monako należą sarna europejska , lis rudy i łasica pospolita .
Podział administracyjny .
Monako stanowi administracyjnie jedność ( nie występują żadne jednostki administracyjne ) .
Jednak do lat 90. kraj dzielił się na 4 dystrykty : Monaco-Ville , Monte Carlo , La Condamine i Fontvieille .
System polityczny .
Księstwo Monako jest monarchią konstytucyjną .
Stan taki rozpoczął się w 1911 roku , kiedy to przyjęto konstytucję tego państwa .
Na jej mocy książę zachował pozycję głowy państwa , ale szereg uprawnień przeszło w ręce innych ciał i instytucji .
Książę .
Książę jest samodzielnym władcą i głową państwa .
Jak dotychczas , wszyscy książęta Monako pochodzili z dynastii Grimaldi .
Od 2005 r. księciem jest Albert II .
Władza ustawodawcza dzielona jest pomiędzy księcia i Radę Narodową .
Władca wnosi projekty ustaw , a Rada przyjmuje je lub odrzuca w drodze głosowania .
Władza wykonawcza spoczywa niemal całkowicie w rękach księcia – Minister Stanu i Rada Rządowa są bezpośrednio odpowiedzialni przed księciem za kierunek podejmowanych działań .
Również władza sądownicza jest częściowo zależna od księcia – sądy wydają wyroki w jego imieniu .
Rada Rządowa .
Rada Rządowa jest ciałem wspierającym księcia Monako w rządzeniu .
Składa się z sześciu członków .
Przewodniczącym Rady jest `` Minister Stanu '' , posiadający podobną pozycję jak we Francji premier , będący tak naprawdę `` „ pierwszym pomiędzy równymi ” '' ( `` primus inter pares '' ) .
W skład rady wchodzi także pięciu radców :
Rada Narodowa .
Rada Narodowa jest parlamentem Księstwa Monako .
Składa się z jednej izby ( unikameralizm ) .
W jej skład wchodzi 24 członków , wybieranych w wyborach powszechnych na kadencję trwającą 5 lat .
Książę może rozwiązać parlament w każdym momencie , co implikuje nowe wybory , które muszą odbyć się w przeciągu 3 miesięcy .
Rada spotyka się co najmniej dwa razy w roku w celu głosowania nad projektem budżetu i projektami aktów prawnych wniesionymi przez rząd księcia .
Partie polityczne .
Partie polityczne w Księstwie Monako :
Rada Koronna .
Ciało administracyjne składające się z 7 członków , zbierające się przynajmniej dwa razy do roku w celu doradzania księciu przy podejmowaniu decyzji w ważnych kwestiach wewnętrznych i międzynarodowych .
Książę powołuje przewodniczącego rady i trzech innych członków , pozostała trójka wybierana jest przez monarchę spośród kandydatów wysuniętych przez Radę Narodową .
Książę musi skonsultować się z Radą Koronną przed podpisaniem umów międzynarodowych , rozwiązaniem Rady Narodowej , przyznaniem obywatelstwa i innymi ważnymi decyzjami .
Sądownictwo .
Sądy w Monako obsadzane są przez księcia .
Podobnie jest z Sądem Najwyższym ( `` Tribunal Supreme '' ) , w którym sędziów mianuje monarcha , część spośród kandydatów przedstawionych przez Radę Narodową .
Państwo-miasto .
Ponieważ Księstwo Monako jest jednocześnie państwem i miastem , równolegle z instytucjami państwowymi funkcjonują instytucje miejskie o podobnych kompetencjach i zakresie działań .
Przykładem tutaj może być Rada Komunalna – ciało odpowiedzialne za administrowanie czterema dzielnicami miasta .
Składa się z 15 członków , wybieranych w wyborach powszechnych na 4 lata , i burmistrza , wybieranego przez tę piętnastkę .
Rada zbiera się co 3 miesiące .
Polityka zagraniczna .
Monako jako w pełni niepodległe oraz suwerenne państwo ma własną politykę zagraniczną .
Jest mocno związane z Francją , z którą utrzymuje bliskie stosunki dyplomatyczne .
Francja w 1919 roku wraz z podpisaniem traktatu wersalskiego zobowiązała się do obrony suwerenności Monako oraz ochrony kraju przed agresją militarną .
W zamian za ochronę Monako miało zagwarantować Francji strefę wpływów .
Traktat z Wersalu został zreformowany podczas spotkań przedstawicieli obu krajów w roku 1945 oraz 1963 .
W 2002 roku Monako podpisało z Francją nową umowę , która weszła w życie w 2005 roku .
