Język mongolski
Język mongolski ( mong . )
– główny język z rodziny mongolskiej .
W węższym znaczeniu nazwa ta odnosi się do dialektu chałchaskiego , będącego podstawą współczesnego standardu literackiego i faktycznie pełniącego funkcję języka urzędowego Mongolii ( z tego względu język mongolski bywa czasem nazywany także `` chałcha-mongolskim '' albo po prostu `` chałchaskim '' ) .
W szerszym znaczeniu nazwa ta obejmuje także szereg dialektów ludności mongolskiej z terenów niepodległego państwa mongolskiego , niektórych obszarów Rosji oraz niektórych prowincji Chińskiej Republiki Ludowej , m.in .
dialekty : czacharski , uracki , chorczyn-tumucki , chorczyński , udżumczyński , ordoski .
Całkowita liczba osób mówiących językiem mongolskim w szerszym znaczeniu szacowana jest na ok. 7 milionów .
Z punktu widzenia typologii wykazuje charakter aglutynacyjny .
Charakteryzuje się też harmonią wokaliczną .
