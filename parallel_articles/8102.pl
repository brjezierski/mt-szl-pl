Język mongorski
Język mongorski , także tu ( `` tǔzúyǔ '' ) – język z rodziny mongolskiej , używany przez mniejszość narodową Tu w Chinach w prowincjach Gansu i Qinghai .
Jest najbardziej zbliżony do języków bao ’ an , dongxiang i mongolskiego .
Wyróżnia się dwa główne , słabo wzajemnie zrozumiałe dialekty : huzhu ( na północy , ok. 60 tys .
użytkowników ) i minhe ( w płd .
części terytorium , ok. 30 tys . ) .
