Mądrzechowo
Mądrzechowo ( dodatkowa nazwa w j. kaszub .
Mądrzechòwò ; niem .
`` Mangwitz '' ) – wieś kaszubska w Polsce na Pojezierzu Bytowskim przy 20 ze Stargardu do Gdyni , położona w województwie pomorskim , w powiecie bytowskim , w gminie Bytów .
Bliskość miasta powiatowego przyśpiesza przekształcanie się Mądrzechowa w południowo-wschodnie przedmieście Bytowa .
Około dwustu metrów na południowy zachód od wsi przebiega nieczynna w chwili obecnej ( stan na 11.11.2006 ) linia kolejowa łącząca Bytów z Lipuszem .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
