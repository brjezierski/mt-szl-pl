Mongolia
Mongolia ( mong .
Монгол улс , trb .
: `` Mongol uls '' ; pismo mongolskie : , trb .
: `` Monggol Ulus '' ) – państwo śródlądowe w środkowowschodniej Azji .
Od północy graniczy z Rosją , a od wschodu , zachodu i południa z Chinami .
Stolicą i największym miastem jest Ułan Bator , gdzie zamieszkuje ok. 47 % populacji całego kraju .
Mongolia jest republiką parlamentarną .
Geografia .
Długość granic lądowych .
Brak dostępu do morza .
Klimat .
Klimat Mongolii w związku z jej położeniem w głębi kontynentu , jest surowy , zaliczany do klimatu umiarkowanego o typie kontynentalnym .
Lato jest tutaj bardzo gorące ( temperatury powyżej +30 °C ) , natomiast zima bardzo mroźna ( poniżej –30 °C ) .
Opady atmosferyczne występują tutaj niezwykle rzadko ( około 300 mm rocznie ) , większość opadów rocznych przypada na dwa miesiące ( od połowy czerwca do połowy sierpnia ) czego wynikiem jest nie wiosenna , lecz letnia wegetacja .
Zima jest z małą pokrywą śnieżną albo prawie bez pokrywy śnieżnej .
Tylko największe rzeki nie zamarzają do dna .
Czasem zimą może przyjść ocieplenie , pokrywa śnieżna po roztopieniu staje się lodem przy powrocie zimna , co powoduje tzw .
`` dzud '' , kiedy bydło ginie z powodu niedostępności zeszłorocznej trawy .
W ciągu trzech kolejnych zim ( 1999 , 2000 , 2001 ) z powodu `` dzudu '' padły miliony sztuk bydła .
Długość lata i zimy jest prawie taka sama ( po ok. 5 miesięcy ) , a długość wiosny i jesieni jest bardzo krótka ( po 1 miesiącu ) .
Skutki zmieniającego się klimatu .
Globalne ocieplenie znacząco wpływa na Mongolię .
Między 1940 a 2001 r. średnia roczna temperatura powietrza wzrosła o ponad 1,5 stopnia Celsjusza .
W tym okresie temperatura zimowa wzrosła nawet o ponad 3,6 stopnia .
Historia .
Najwcześniejsze informacje o dziejach Mongolii pochodzą z chińskich kronik w których pojawiły się wzmianki o ludzie meng-ku najeżdżającym ówczesne Chiny .
W IV wieku p.n.e .
na terenach zamieszkanych przez mongolskich , tureckich i tunguskich koczowników powstało silne państwo Xiongnu , którego mieszkańcy przez wielu naukowców uważani są za przodków Hunów .
Rozpadło się ono w I wieku p.n.e .
w wyniku walk wewnętrznych i naporu chińskiej armii .
Hunowie ( jeśli to było ich państwo ) powędrowali na zachód i w III wieku n.e .
dotarli do Europy , przyczyniając się wraz z Gotami i Wandalami do upadku Rzymu .
Południową część Xiongnu zagarnęły Chiny , a północną objęli we władanie tureccy i mongolscy koczownicy .
Byli wśród nich między innymi lud Rouran , którzy według niektórych teorii byli przodkami Awarów , którzy w VI wieku najechali Europę .
W tym samym czasie mieszkańcy Mongolii uwolnili się spod chińskiej dominacji .
W XI wieku władzę nad stepami przejął protomongolski szczep Kitanów .
Ich panowanie zakończyło się w 1125 roku , gdy podbił ich tunguski szczep Dżurdżenów , którego sprzymierzeńcem byli Tatarzy .
W roku 1198 Temudżyn objął tron i nadano mu tytuł chana wszystkich chanów ( Czyngis-chana ) .
Czyngis podporządkował sobie tereny od Wielkiego Muru po jezioro Bajkał na północy i od Ałtaju na wschodzie do gór Chingan na zachodzie i w 1206 r. proklamował Wielkie Imperium Mongolskie .
Połączone siły Mongołów prowadzone przez Czyngisa podbiły północne Chiny , Imperium Choroezimskie i tereny Azji Centralnej , sprowadzając do swojej stepowej stolicy Karakorum przebogate łupy .
Następcy Temudżyna zagarnęli Ruś , dzisiejsze : Afganistan , Iran i Irak , resztę Chin oraz Tybet , tworząc tym samym jedno z największych państw w historii świata – imperium mongolskie .
Rządzenie tak gigantycznym terytorium było niezmiernie trudne , więc mongolskie imperium zostało podzielone około 1261 roku na trzy części .
Mongolia znalazła się w jednej części z Chinami pod władzą mongolskiej dynastii Yuan .
W roku 1264 władca tego państwa Kubilaj przeniósł stolicę z Karakorum do Chanbałyku ( dzisiejszy Pekin ) .
Władza Mongołów w Chinach zakończyła się w roku 1368 , gdy w wyniku antymongolskiego powstania władzę w Chinach przejęła dynastia Ming .
Pięć lat później chińskie wojska zrównały z ziemią Karakorum .
Południowa część Mongolii ( obecna Mongolia Wewnętrzna ) stała się częścią Chin , a reszta kraju rozpadła się na mniejsze państwa , które sporadycznie się jednoczyły .
W latach 1626–1636 Mongolię Wewnętrzną opanowali Mandżurowie .
Mongolię Zewnętrzną opanowali 55 lat później , będąc już wtedy władcami całych Chin .
Uzyskała ona status federacji plemion pod protektoratem chińskim .
Na początku XX wieku Chiny zaczęły zacieśniać swoją kontrolę nad Mongolią Zewnętrzną i rozpoczęły jej kolonizację .
Spowodowało to zwrócenie się mongolskich książąt w stronę Rosji .
W 1911 wybuchła w Chinach antycesarska rewolucja .
Korzystając z zamętu , Mongolia Zewnętrzna ogłosiła przy wsparciu Rosji niepodległość .
Utworzono teokratyczny rząd na czele z Bogda Chanem .
Po rosyjsko-chińskich negocjacjach Mongolia Zewnętrzna została w 1915 na mocy traktatu kiachtańskiego uznana za część Chin , jednak rząd chiński zgodził się na jej autonomię .
Gdy w Rosji wybuchła rewolucja komunistyczna do Mongolii Zewnętrznej wkroczyły wojska chińskie i obaliły w 1919 Bogda Chana .
W 1921 roku armia antybolszewickich Rosjan pod wodzą von Ungern-Sternberga wraz z nielicznymi oddziałami książąt mongolskich zdobyła stolicę kraju Urgę ( obecnie Ułan Bator ) i opanowała większość kraju , zapoczątkowując ogólnonarodowe powstanie przeciw Chinom .
Na tronie ponownie osadzony został Bogda Chan .
W lipcu 1921 Armia Czerwona , wspomagając rewolucję mongolską Damdina Suche Batora , zajęła Mongolię Zewnętrzną , rozbijając wojska Ungerna i chińskie .
Ustanowiony został rząd komunistyczny , jednak zachowano ustrój teokratyczny i władzę Bogda Chana .
W 1924 , po śmierci Bogda Chana , proklamowano utworzenie Mongolskiej Republiki Ludowej .
Mimo formalnej niepodległości kraj pozostawał pod ścisłą kontrolą władz w Moskwie , a przez wiele państw świata nadal był uważany za część Chin .
Już po zwycięstwie rewolucji doszło do konfliktu między Suche Batorem a lewicowym skrzydłem rządzącej Mongolskiej Partii Ludowej na czele z Dogsomynem Bodoo .
Frakcja Bodoo optowała za budową socjalizmu w Mongolii , ale także za uniezależnieniem się od ZSRR .
W grudniu 1921 Bodoo został zwolniony ze wszystkich stanowisk rządowych i usunięty z partii , a w sierpniu 1922 aresztowany przez władze centralne i wraz z większością towarzyszy skazany na śmierć i zabity we wrześniu 1922 .
W kolejnych latach władzę w partii sprawowali zwolennicy partyjnej prawicy ( NEP-owcy ) .
Na początku lat 30. z inicjatywy polityków tj .
Chorlogijn Czojbalsani i Peldżidijn Genden miała miejsca wolta w lewą stronę i przyśpieszenie radykalnych reform gospodarczych w tym kolektywizacji .
Reformy nie udały się i w efekcie w 1932 roku doszło do nieudanego buntu wojskowych na prowincji .
Bunt doprowadził do odsunięcia ( z inicjatywy Moskwy ) części działaczy `` lewicowego odchylenia '' od głównego nurtu polityki , a następnie ich egzekucji .
Okres czystki przeciwko lewicy partyjnej przetrwał Peldżidijn Genden który jeszcze w 1932 roku został premierem , a w trakcie rządów odsunął na bok swoje lewicowe tendencje , wprowadzając założenia `` nowej polityki reform '' polegającej na złagodzeniu komunistycznych zasad ekonomicznych .
Reforma była wzorowana na Nowej Polityce Ekonomicznej .
Na gruncie reform zredukował podatki , zwiększył liczbę prywatnych przedsiębiorstw oraz pozwolił na normalne funkcjonowanie instytucji religijnych .
Wraz ze wzrostem gospodarczym i zmniejszeniem niedoborów wzrosła popularność Gendena w społeczeństwie .
W 1934 roku rozpoczął się spór Gendena ze Stalinem .
Na jednym ze spotkań , radziecki przywódca naciskał na Gendena , aby ten zniszczył kler buddyjski poprzez eksterminację lamów .
Geden stanowczo się temu sprzeciwił ponieważ sam był praktykującym buddystą .
Premier zapowiedział , że nie będzie walczył z religią i pozwolił lamom na dowolne głoszenie swoich poglądów .
Genden działał także na rzecz zakończenia stacjonowania wojsk radzieckich w Mongolii .
Premier próbował wykorzystać napięcia na linii ZSRR-Japonia na korzyść Mongolii i uwolnienia się spod dominacji radzieckiej .
W 1936 roku Stalin doprowadził do aresztowania Gendena pod zarzutem szpiegostwa na rzecz Japonii .
Po aresztowaniu Gendena władzę w kraju objął Chorlogijn Czojbalsan .
