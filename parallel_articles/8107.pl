Montana
Montana ( ) – stan w zachodniej części Stanów Zjednoczonych , przy granicy z Kanadą .
W 2012 roku osiągnął 1 milion mieszkańców i jest jednym z najsłabiej zaludnionych .
Nazywany jest `` Treasure State '' ( Stan Skarbów ) ze względu na jego ogromne bogactwo mineralne .
Nazwa prawdopodobnie pochodzi od hiszpańskiego słowa „ montaña ” , które znaczy „ góra ” .
Chociaż Montana kojarzona jest przede wszystkim z położonymi na jej obszarze górami , około 60 % terytorium stanu pokrywa preria , wchodząca w skład Wielkich Równin .
Region nazywany jest `` Stanem Skarbów '' , `` Krainą lśniących gór '' i `` Krainą nieba '' .
Montana jest czwartym co do wielkości stanem Stanów Zjednoczonych , co w połączeniu ze stosunkowo małym zaludnieniem plasuje ją wśród regionów o wyjątkowo niskiej gęstości zaludnienia .
Gospodarka Montany bazuje na rolnictwie , górnictwie i przemyśle drzewnym .
Duże znaczenie ma także rozwinięta turystyka .
W Montanie znajduje się Park Narodowy Glacier oraz północny , niewielki fragment Parku Narodowego Yellowstone .
Przez stan przechodzi wododział kontynentalny Ameryki .
Historia .
Montana stała się integralną częścią Stanów Zjednoczonych 26 maja 1864 .
W 1889 r. , kiedy stała się 41. stanem USA , słynęła z hodowli bydła i zaczęła wyłaniać się jako jeden z wiodących ośrodków wydobycia miedzi w kraju .
Montana była ostatnim silnym punktem oporu Indian przed kolonizatorami z Europy .
Montana była pierwszym stanem , który wybrał kobietę do Kongresu .
Jeannette Rankin została wybrana w 1916 , a następnie ponownie w 1940 i była jedynym członkiem Kongresu , który głosował przeciwko udziałowi USA w obu wojnach światowych .
