N.N. , NN , n/n – skrótowiec stosowany głównie w terminologii prawniczej i kryminalistyce na określenie osoby , której tożsamości nie ustalono .
Pochodzi od łacińskich słów `` nomen nescio '' ( dosł .
imienia nie znam ) , `` non notus '' ( nieznany ) lub `` nomen nominandum '' ( imię do ustalenia ) .
W języku polskim przyjęło się rozwijać NN do `` nazwisko nieznane '' .
Termin ten wywodzi się ze starożytnego Rzymu , gdzie był używany przy i do oznaczenia osoby występującej w kazusie przeznaczonym dla adeptów nauki prawa .
