NLE Choppa
Bryson Lashun Potts ( ur .
1 listopada 2002 w Memphis ) , znany pod pseudonimem NLE Choppa – amerykański raper i autor tekstów .
Zasłynął singlem „ Shotta Flow ” , który ukazał się w styczniu 2019 roku .
Utwór zajął 36. miejsce w rankingu Bilboard Hot 100 .
W późniejszym czasie NLE Choppa wydał remix piosenki z artystą o pseudonimie Blueface .
Wydał również takie utwory jak „ Camelot ” , „ Walk Em Down ” z raperem Roddy Ricch i „ Ruff Rydas ” .
