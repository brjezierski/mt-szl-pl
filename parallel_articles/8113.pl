Języki północno-wschodniokaukaskie
Języki północno-wschodniokaukaskie , inaczej języki kaspijskie lub nach-dagestańskie – rodzina języków używanych głównie w Czeczenii , Dagestanie i Azerbejdżanie .
Języki te bywają łączone z północno-zachodniokaukaskimi w ramach tzw .
rodziny północnokaukaskiej .
Cechują się skomplikowaną fonetyką ( do 60 fonemów spółgłoskowych i 30 samogłoskowych ) , ergatywną strukturą zdania , obecnością klas nominalnych i rozbudowanymi systemami deklinacji .
Przypuszcza się , że do rodziny nach-dagestańskiej należały również starożytne języki hurycki i urartyjski .
