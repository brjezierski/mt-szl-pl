Języki nachskie
Języki nachskie – nieliczna grupa języków , używanych na Kaukazie Północnym , w Czeczenii i Inguszetii oraz w kacheckiej Zemo-Alweni .
Liczba użytkowników waha się , w zależności od źródła , od 1,1 mln do 1,7 mln .
Początkowo uważano je za odrębną rodzinę językową – nachską lub północno-środkowo kaukaską , jednak obecnie zazwyczaj klasyfikuje je się jako podrodzinę języków nach-dagestańskich .
