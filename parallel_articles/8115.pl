Nocnik
Nocnik – zbiornik trzymany zwyczajowo pod łóżkiem , używany zazwyczaj jako urynał .
Nocniki są często ceramiczne i mają pokrywki .
Inne materiały to tworzywa sztuczne , miedź , srebro , porcelana ( bourdaloue ) , szkło ( Kuttrolf ) , nocniki drewniane w kształcie rur wykorzystywane w Turkmenistanie .
Szczególną formę nocników są tzw .
`` Bourdaloue '' wykorzystywane przez kobiety .
Nocniki te były bogato zdobione , czasami z napisem `` Au
plaisir des dames ... '' ( ku uciesze dam ) .
Podobno nazwa pochodzi od francuskiego katolickiego księdza Louisa Bourdaloue , który wygłaszał długie kazania i arystokratki dyskretnie oddawały mocz do nocnika , co pozwalało na dłuższe kontemplowanie kazania .
Po wynalezieniu klozetu wodnego w XIX wieku nocniki straciły swoją popularność .
W starożytnej Grecji i Rzymie uryna była zbierana z urynałów i wykorzystywana do wybielania pościeli ( fermentacja moczu produkuje amoniak ) .
Rzymska nazwa to `` matula '' , `` matella '' , `` matellio '' .
Bogaci Rzymianie posiadali nocniki ze srebra i złota i prawo rzymskie przewidywało sposoby dziedziczenia tych urządzeń .
W Anglii nocniki są dawane jako prezent ślubny .
Obecnie coraz częściej spotyka się nocniki plastikowe , nieoddające w niczym przepychu dawnych dni .
Obecnie nocnikiem nazywa się przedmiot , najczęściej wykonany z tworzyw sztucznych , używany przez dzieci do oddawania moczu lub kału w fazie przejściowej pomiędzy używaniem pieluszek a toalety .
Nocnik jest też nazywany kaczką , urynałem .
