Naczysławki
Naczysławki ( dodatkowa nazwa w j. niem .
Klein Nimsdorf ) – wieś w Polsce położona w województwie opolskim , w powiecie kędzierzyńsko-kozielskim , w gminie Reńska Wieś .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
W Naczysławkach urodził się Wilhelm Kloske , polski duchowny rzymskokatolicki , biskup pomocniczy gnieźnieński w latach 1911–1924 .
Nazwa .
Według niemieckiego językoznawcy Heinricha Adamy ’ ego nazwa ma charakter patronimiczny i wywodzi się od imienia - `` Ignacy '' .
Pochodzi ona od założyciela lub patrona wsi .
W swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu jako starszą od niemieckiej wymienia polską formę nazwy - `` Nacislawice '' podając jej znaczenie `` Dorf der St. Ignatius '' - `` Wieś św .
Ignacego '' .
Nazwa została zgermanizowana przez Niemców na `` Nimsdorf '' , a później na `` Klein Nimsdorf '' i utraciła swoje pierwotne znaczenie .
