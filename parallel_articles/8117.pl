Nadarzyn ( Kobyłka )
Nadarzyn – dzielnica Kobyłki , w województwie mazowieckim , w powiecie wołomińskim .
Leży w południowej części Kobyłki ; na południe od linii kolejowej i na zachód od lasu Kacze Doły .
Do 1957 samodzielna wieś .
Jeden z najstarszych przysiółków kobyłeckich , istniejących jeszcze przed parcelacją majątku .
W latach 1867–1928 wieś w gminie Radzymin , a 1928–1954 w gminie Kobyłka w powiecie radzymińskim .
20 października 1933 utworzył gromadę w granicach gminy Kobyłka , składającą się z wsi Nadarzyn i cegielni Dyonizy .
Od 1 lipca 1952 w powiecie wołomińskim .
W związku z reorganizacją administracji wiejskiej jesienią 1954 Nadarzyn wszedł w skład gromady Kobyłka .
1 stycznia 1957 gromadę Kobyłka przekształcono w osiedle , przez co Nadarzyn stał się integralną częścią Kobyłki , a w związku z nadaniem Kobyłce praw miejskich 1 stycznia 1969 – częścią miasta .
Ulice .
W granicach Nadarzyna znajdują się następujące ulice :
Borysów , Ceglana , Majdańska , Nadarzyn , Ossowska , Ks .
Józefa Poniatowskiego , Turowska .
