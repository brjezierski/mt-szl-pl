Augustina Weltzla „ Górnośląski Tacyt ” – coroczna nagroda przyznawana od 2007 roku badaczom i popularyzatorom historii Górnego Śląska .
Patronem jest XIX–wieczny historyk , pionier górnośląskiej historiografii , ks .
Augustin Bogislaus Weltzel , nazywany „ Górnośląskim Tacytem ” .
Nagrodę ustanowił Ruch Autonomii Śląska .
Statuetka wręczana laureatom przedstawia postać patrona z atrybutami Klio .
Jej autorem jest rzeźbiarz August Dyrda .
