Nadkole
Nadkole – wieś w Polsce znajdująca się w województwie mazowieckim , w powiecie węgrowskim , w gminie Łochów .
Charakteryzuje się malowniczymi krajobrazami , położona między dwoma rzekami : Liwcem i Bugiem .
Nadkolski las jest częścią Nadbużańskiego Parku Krajobrazowego , w obszarze dawnej Puszczy Kamienieckiej .
Wieś królewska położona była w drugiej połowie XVI wieku w powiecie kamienieckim ziemi nurskiej województwa mazowieckiego , wg niektórych podań – należąca do dóbr rażnieńskich .
Nazwa wsi w tej formie pojawia się już w źródłach XVI-wiecznych .
W połowie XVII w. wieś , wraz z okolicznymi dobrami kamieńczykowskimi , przekazano na podstawie uchwały sejmowej klasztorowi sióstr Wizytek .
W latach II Wojny Światowej tereny te znalazły się pod okupacją niemiecką , lecz nazwa wsi nie została zniemczona , w czasie walk wieś była częściowo spalona .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
