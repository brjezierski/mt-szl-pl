Ναύπακτος ; starogr .
`` Naupaktos '' ; w okresie panowania weneckiego `` Lepanto '' ) – miasto w środkowej Grecji , na północny wschód od miasta Patras , port u wejścia do Zatoki Korynckiej , w administracji zdecentralizowanej Peloponez , Grecja Zachodnia i Wyspy Jońskie , w regionie Grecja Zachodnia , w jednostce regionalnej Etolia-Akarnania , siedziba administracyjna gminy Nafpaktia .
W 2011 roku liczyło 13 415 mieszkańców .
