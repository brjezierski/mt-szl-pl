Nagykutas
Nagykutas – wieś i jednocześnie gmina na Węgrzech w komitacie Zala , w powiecie Zalaegerszeg na południowym zachodzie kraju .
Położenie i geografia .
Miejscowość leży w północnej części komitatu , przy granicy z komitatem Vas , 8 km na północny zachód od Zalaegerszegu .
Główną ulicą przebiega droga nr 7404 z Zalaegerszegu do Andrásfa , leżącego w południowej części komitatu Vas .
W szczytowej części łańcucha wzgórz rozciągającego się na południe od wsi w XVIII i XIX wieku wybudowano ciąg piwnic winnych z bali , krytych spadzistymi dachami .
Tu znajdują się największe sady jabłoniowe w komitacie Zala .
W centrum wsi wybudowano w latach 40 .
XVIII wieku kościół , który został odnowiony w 2002 r. Oprócz tego w 1992 r. powstał tu dom gminny .
