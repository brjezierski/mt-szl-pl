Nagykálló
Nagykálló ( jidysz `` Kalov , Kaluv , Kalev , Kaliv '' ) – miasto powiatowe w komitacie Szabolcs-Szatmár-Bereg w północno-wschodnich Węgrzech .
Położenie .
Nagykálló leży w centrum wysoczyzny Nyírség .
Lokalne drogi łączą miasto z Nyíregyháza , Nyírbátor , Nyírtura , Újfehértó , Téglás i Nyíradony .
W Nagykálló lokalna linia kolejowa z Nyíregyháza rozgałęzia się do Nyírbátor i do Nyíradony .
Historia .
Pierwsza pisemna wzmianka o Nagykálló pochodzi z roku 1274 .
W 1315 król Karol Robert podniósł miejscowość do rangi osady targowej .
