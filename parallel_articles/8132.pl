Naharijja ( stacja kolejowa )
Naharijja ( hebr .
: נהריה ) – stacja kolejowa w mieście Naharijja , w Izraelu .
Jest obsługiwana przez Rakewet Jisra ’ el .
Stacja znajduje się we wschodniej części miasta Naharijja .
Dworzec jest przystosowany dla osób niepełnosprawnych .
Połączenia .
Pociągi z Naharijji jadą do Akki , Hajfy , Tel Awiwu , Lod , Modi ’ in , Rechowot , Aszkelonu i Beer Szewy .
