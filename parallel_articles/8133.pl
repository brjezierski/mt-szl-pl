Nainital ( miasto )
Nainital – miasto w północnych Indiach w stanie Uttarakhand , na pogórzu Himalajów Małych .
Jest położone na wysokości 2084 m n.p.m .
tuż przy górze o nazwie Naina o wysokości 2615 m n.p.m .
Populacja miasta w 2001 roku wynosiła 38 560 mieszkańców .
Nainital ma swoje miejsce w mitologii indyjskiej dzięki świętemu jezioru o tej samej nazwie a także świątyni bogini Śakti .
O Nainital pisał w swoich powieściach Rudyard Kipling .
