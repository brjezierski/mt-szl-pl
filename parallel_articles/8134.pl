Nairobi ( hrabstwo )
Nairobi – jedno z 47 hrabstw Kenii , do 2013 roku miało status prowincji .
Liczy 4,4 mln mieszkańców ( 2019 ) .
Jest najbardziej zaludnionym hrabstwem Kenii .
Hrabstwo znajduje się na rodzimych ziemiach ludu Kikuju , ale imigracja przyniosła tutaj wszystkie inne społeczności właściwe dla Kenii .
Nairobi graniczy na południu i południowym zachodzie z hrabstwem Kajiado , na północy i zachodzie z hrabstwem Kiambu , a na wschodzie z hrabstwem Machakos .
Gospodarka .
Nairobi jest stolicą Kenii i jednym z kluczowych domów dla afrykańskich instytucji finansowych , biznesowych , transportowych , komunikacyjnych , pozarządowych i dyplomatycznych .
Dzięki znanej na całym świecie branży turystyki dzikiej przyrody miasto znane jest jako „ stolica safari na świecie ” .
Według raportu Krajowego Biura Statystycznego Kenii , w latach 2013–2017 hrabstwo średnio wytwarzało 21,7 % PKB całego państwa .
