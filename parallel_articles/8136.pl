Najświętsza Maryja Panna Królowa Polski
Najświętsza Maryja Panna `` Królowa Polski '' – tytuł Marii z Nazaretu , używany przez polskich katolików .
Kult Maryi jako `` Najświętszej Maryi Panny Królowej Polski '' wyróżnia Kościół katolicki spośród innych wyznań chrześcijańskich w Polsce .
Kult ma charakter ogólny i występuje w Polsce oraz w polskich parafiach przy skupiskach Polonii .
Tytuł `` NMP Królowej Polski '' mówi o obecności NMP w historii polskiego narodu .
Jan Długosz nazwał Maryję `` Panią świata i naszą '' .
Tytuł Matki Bożej jako Królowej Polski sięga drugiej połowy XVI wieku .
Wtedy to Grzegorz z Sambora po raz pierwszy nazywa Maryję Królową Polski i Polaków .
1 kwietnia 1656 r. w katedrze lwowskiej król Jan Kazimierz w sposób oficjalny ślubował przed wizerunkiem Matki Bożej Łaskawej : „ Ciebie za patronkę moją i za Królową państw moich dzisiaj obieram ” .
W trzechsetną rocznicę ślubów Jana Kazimierza episkopat Polski z inicjatywy więzionego prymasa Stefana Wyszyńskiego dokonał ponownego zawierzenia całego kraju Maryi i odnowienia ślubów królewskich .
26 sierpnia 1956 roku ok. miliona wiernych na Jasnej Górze złożyło Jasnogórskie Śluby Narodu Polskiego .
3 maja w Kościele katolickim w Polsce obchodzona jest liturgiczna uroczystość Najświętszej Maryi Panny Królowej Polski .
Matka Boża Królowa Polski jest główną patronką archidiecezji częstochowskiej i przemyskiej , była też patronką archidiecezji lwowskiej .
Historia .
Historia rozkwitu kultu Maryi Królowej Polski wiąże się z okresem kontrreformacji .
Po Neapolu i Krakowie , Wilno było wtedy jezuicko-europejskim centrum rozgłaszania w/w godności .
Obraz Matki Bożej Ostrobramskiej z 1620 r. stał się pierwszym wcielonym skutkiem objawień .
Odejście Szwedów od Jasnej Góry , o którym król Jan Kazimierz dowiedział się w Krośnie , wpłynęło na nazwanie Matki Bożej Królową Polski .
