Nakkila ( stacja kolejowa )
Nakkila ( fiń : `` Nakkilan rautatieasema '' ) – stacja kolejowa w Nakkila , w regionie Satakunta , w Finlandii .
Znajduje się na linii Tampere – Pori .
Została otwarta w 1895 .
Budynek dworca był wielokrotnie przebudowywany .
Sama stacja została zamknięta 1983 roku .
Obecnie w budynku dworca działa pchli targ .
