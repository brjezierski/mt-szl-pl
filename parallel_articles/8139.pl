Naklik
Naklik – wieś w Polsce położona w województwie lubelskim , w powiecie biłgorajskim , w gminie Potok Górny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 435 mieszkańców i była szóstą co do wielkości miejscowością gminy Potok Górny .
Wieś stanowi sołectwo .
W latach 1648–1935 funkcjonował Folwark Naklicki .
19 marca 1863 roku obozował oddział płk Leona Czechowskiego , a w nocy z 9 na 10 maja 1863 roku przeszedł oddział gen. Antoniego Jeziorańskiego , któremu towarzyszył gen. Aleksander Waligórski .
W 1953 powstała Ochotnicza Straż Pożarna .
W latach 1993–1996 zbudowano kościół filialny `` pw .
Miłosierdzia Bożego '' ( filialny parafii Potok Górny ) .
