Nakory
Nakory – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Suchożebry .
Wieś posiadała w 1673 roku podkomorzyna koronna Konstancja Butlerowa , leżała w ziemi drohickiej województwa podlaskiego w 1795 roku .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Według Narodowego Spisu Powszechnego Ludności i Mieszkań z 2011 roku liczba ludności we wsi Nakory to 244 , z czego 47,1 % mieszkańców stanowią kobiety , a 52,9 % ludności to mężczyźni .
Miejscowość zamieszkuje 5,1 % mieszkańców gminy
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Częstochowskiej w Krynicy .
Atrakcje .
We wsi Nakory znajduje się park linowy czynny w okresie letnim oraz Ptaszarnia mini-zoo , w którym można znaleźć tak egzotyczne gatunki , jak paw jawajski , uszak biały , tragopan tyminek , cyraneczka bajkalska czy sterniczka jamajska .
W niezwykłą podróż wśród ptaków z całego świata w hodowli państwa Czarnockich może się wybrać dosłownie każdy .
