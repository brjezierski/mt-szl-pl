Nakuru ( hrabstwo )
Nakuru – hrabstwo w Kenii .
Jego stolicą i największym miastem jest Nakuru .
Inne ważniejsze ośrodki miejskie to : Naivasha , Gilgil , Molo , Rongai , Njoro , Elburgon i Mai Mahiu .
Według Spisu Powszechnego w 2019 roku liczy 2 162 202 mieszkańców i jest jednym z najbardziej zaludnionych hrabstw Kenii .
Miejscowi ludzie są w większości pochodzenia etnicznego Kikuju .
