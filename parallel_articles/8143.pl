Wola Nakonowska
Wola Nakonowska – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie włocławskim , w gminie Choceń .
W latach 1975–1998 miejscowość administracyjnie należała do województwa włocławskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 273 mieszkańców .
Jest dziewiątą co do wielkości miejscowością gminy Choceń .
