Nalczyk
Nalczyk ( ros .
Нальчик ) – miasto w Rosji , stolica Republiki Kabardyjsko-Bałkarskiej .
Powierzchnia miasta wynosi 67 km² ( według innych źródeł 131 ) , a w 2020 roku liczyło 239 583 mieszkańców .
Historia .
Nazwa miasta pochodzi od rzeki Nalczyk , nad którą miasto jest ulokowane .
Słowo „ Nalczyk ” oznacza „ małe kopyto ” w języku bałkarskim i kabardyńskim .
W 1822 roku Rosjanie założyli fort Nalczyk .
Prawa miejskie miejscowość uzyskała w 1921 , kiedy to stała się centrum Kabardyjskiego Okręgu Autonomicznego .
Podczas II wojny światowej , od 28 października 1942 do 3 stycznia 1943 , Nalczyk był okupowany przez Niemców .
Gospodarka .
W mieście rozwinął się przemysł elektromaszynowy , hutniczy , chemiczny , spożywczy , materiałów budowlanych , drzewny oraz obuwniczy .
