W mieście działają dwa teatry , dwa muzea oraz uniwersytet .
Historia .
Pierwsze wzmianki o Namanganie pochodzą z XV wieku .
Miejscowość uzyskała prawa miejskie w 1610 roku .
Dzięki rozwojowi rzemiosła Namangan stał się w połowie XVIII wieku jednym z ważniejszych miast w Kotlinie Fergańskiej .
W tym samym wieku miasto zostało przyłączone do chanatu kokandzkiego , a po likwidacji chanatu w 1876 roku zostało włączone do Imperium Rosyjskiego .
W 1926 roku miasto nawiedziło silne trzęsienie ziemi .
