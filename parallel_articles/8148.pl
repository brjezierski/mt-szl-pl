Namysłów ( stacja kolejowa )
Namysłów – stacja kolejowa w Namysłowie , w województwie opolskim , w Polsce .
W czasie II wojny światowej na stacji funkcjonowała zakonspirowana komórka AK pod dowództwem Alfonsa Parzonki .
3 marca 1945 r. do Namysłowa przybyli z Kluczborka pierwsi polscy kolejarze , którzy w następnych miesiącach zaczęli uruchamiać urządzenia stacyjne .
Na początku lat 90. była to stacja przesiadkowa , a z nieistniejącego peronu 3 odchodziły pociągi do Opola przez Jełowę .
Niszczejący i zabytkowy budynek stacyjny , według umowy między PKP Oddział Gospodarowania Nieruchomościami we Wrocławiu i burmistrzem , został przekazany miastu na początku roku 2007 .
Istniejące jeszcze w połowie lat 90. przejście podziemne zasypano i zastąpiono kładką , która dodatkowo umożliwiła bezpieczne wyjście ze stacji w kierunku ulicy Reymonta .
Dodatkowo , na początku roku 2007 zostały zainstalowane kamery obejmujące niemal cały teren stacyjny .
W latach 2010–2012 przeprowadzono gruntowny remont dworca .
Prace remontowe były prowadzone pod ścisłym nadzorem służb konserwatorskich .
Koszt inwestycji wyniósł prawie 5 mln zł .
Uroczyste otwarcie wyremontowanego dworca kolejowo-autobusowego miało miejsce 23 czerwca 2012 roku .
W roku 2018 stacja obsługiwała ok. 1000 pasażerów na dobę .
