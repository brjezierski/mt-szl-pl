Nanaimo
Nanaimo jest drugim co do wielkości miastem na wyspie Vancouver w Kolumbii Brytyjskiej w Kanadzie .
Jest siedzibą jednostki administracyjnej Nanaimo .
Nazwa miasta pochodzi od nazwy zamieszkującego do dziś jego okolice plemienia Snuneymuxw , należącego do grupy plemion Saliszów .
Położenie i geografia .
Miasto leży na wyspie Vancouver .
Nanaimo jest oddalone od miasta Vancouver o około 55 km , od którego oddziela je cieśnina Strait of Georgia .
Miasto ma regularne połączenie promowe z Vancouver .
Dzięki niedalekiej odległości od Vancouver , Nanaimo jest „ bramą wejściową ” do wielu innych miejsc zarówno na głównej wyspie , jak i na Wyspach Zatokowych .
