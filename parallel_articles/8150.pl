Nanajowie
Nanajowie ( ) , dawna rosyjska nazwa Goldowie – lud tunguski zamieszkujący tereny dalekiego wschodu Syberii ( Rosja ) , nad dolnym Amurem i Ussuri .
Liczebność Nanajów wynosi ok. 17 tysięcy , z czego 12 003 żyje w Federacji Rosyjskiej ( według spisu powszechnego z 2010 ) , a 4640 w Chinach ( 2000 ) .
Posługują się językiem nanajskim z grupy tungusko-mandżurskiej .
Od XIX wieku prawosławni , lecz zachowali też pierwotne wierzenia animistyczne oraz szamanizm .
Nanajowie zajmują się myślistwem , rybołówstwem , hodowlą .
Mają dobrze rozwinięte kowalstwo , obróbkę kości i ornamentowanie odzieży .
Goldem był Dersu Uzała , opisany przez rosyjskiego etnografa Władimira Arsienjewa .
