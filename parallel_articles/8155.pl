Nanyang
Nanyang ( chiń .
南阳 ; pinyin : `` Nányáng '' ) – miasto o statusie prefektury miejskiej we wschodnich Chinach , w prowincji Henan .
W 2010 roku liczba mieszkańców miasta wynosiła 251 782 .
Prefektura miejska w 1999 roku liczyła 10 373 217 mieszkańców .
Stolica rzymskokatolickiej diecezji Nanyang .
W pobliżu miasta odkryto skamieniałości jaj dinozaura .
