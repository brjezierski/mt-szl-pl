Napa ( zapięcie )
Napa , zatrzask – zapięcie zastępujące guzik , wykonywane z metalu lub tworzywa sztucznego .
Napa składa się z dwóch części : zewnętrznej i wewnętrznej .
Część zewnętrzna to gniazdo mocowane do odzieży przy pomocy ozdobnego nitu rurkowego .
Ozdobna część nitu widoczna jest na zewnątrz i wyglądem przypomina guzik .
Część wewnętrzna to główka zatrzasku , również mocowana przy pomocy rurkowego nitu .
Napy są stosowane wszędzie tam , gdzie istnieje potrzeba rozłączalnego połączenia dwóch części odzieży .
Stosowane są także jako zapięcia w obuwiu , rękawiczkach i galanterii skórzanej .
