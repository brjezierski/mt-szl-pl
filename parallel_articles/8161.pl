`` Napoleone Buonaparte '' , ur .
15 sierpnia 1769 w Ajaccio na Korsyce , zm .
5 maja 1821 w Longwood na Wyspie Św .
Heleny ) – francuski mąż stanu , dowódca wojskowy i przywódca polityczny , jako Napoleon I cesarz Francuzów i głowa I Cesarstwa Francuskiego w latach 1804–1814 oraz w roku 1815 , wcześniej Pierwszy Konsul Republiki Francuskiej 1799–1804 , prezydent ( 1802–1805 ) , a następnie król Włoch ( 1805–1814 ) , protektor Związku Reńskiego ( 1806–1813 ) , a przez to faktyczny zwierzchnik Księstwa Warszawskiego .
Uważany za najwybitniejszego dowódcę wojskowego w historii , a także za jedną z najważniejszych postaci w historii Francji i świata .
Okres wojen napoleońskich wywarł wpływ niemal w całej Europie .
Młodość i początki kariery wojskowej .
Urodził się jako Napoleone di Buonaparte w Ajaccio na Korsyce w szlacheckiej rodzinie pochodzenia włoskiego .
Syn Carla Marii Buonapartego i jego żony Letycji .
Miał liczne rodzeństwo : braci Józefa , Ludwika , Lucjana i Hieronima , oraz trzy siostry : Karolinę Bonaparte-Murat , Paulinę Bonaparte-Borghese i Elizę Bonaparte-Baciocchi .
21 lipca 1771 roku został ochrzczony jako katolik , w katedrze Ajaccio .
Buonapartowie mieszkali w swoim rodzinnym domu , zwanym Maison Bonaparte .
W styczniu 1779 został zapisany do seminarium duchownego w Autun , gdzie przez trzy miesiące uczył się języka francuskiego .
Rektor seminarium , ksiądz Chardon wspominał go jako poważnego i zamyślonego .
Nie nauczył się jednak poprawnej wymowy francuskiej i mówił z korsykańskim akcentem .
Dzięki protekcji gubernatora Korsyki , 23 kwietnia 1779 roku został przyjęty do szkoły wojskowej w Brienne-le-Château .
Był jednym z pięćdziesięciu królewskich stypendystów – na łączną liczbę stu dziesięciu uczniów .
Wykładane przedmioty to między innymi matematyka , łacina , francuski , niemiecki , historia , geografia , fizyka , budowa fortyfikacji , uzbrojenie , szermierka , taniec i muzyka .
Tam zyskał opinię samotnika , który wolał naukę od towarzystwa lepiej sytuowanych kolegów .
Jego ulubionym zajęciem było czytanie książek .
Często zaszywał się w bibliotece i studiował dzieła Polibiusza , Plutarcha , Flawiusza Arriana i Kwintusa Rufusa .
Od 1784 kontynuował naukę w l ’ École Militaire w Paryżu .
Po zakończeniu nauki w 1786 ze stopniem podporucznika został przydzielony do służby w artylerii .
Po śmierci ojca , w wieku 16 lat zajął się bratem Ludwikiem , który odtąd z nim mieszkał w różnych koszarach i pobierał od niego nauki .
Po wybuchu rewolucji francuskiej nie od razu opowiedział się po jej stronie .
Zgłosił się do korsykańskich powstańców Paolego , w konsekwencji odmowy przyjęcia przeszedł na stronę francuską .
W kwietniu 1791 został awansowany na porucznika .
Kiedy Paoli w maju 1793 wywołał antyfrancuskie powstanie , rodzina Bonapartych musiała uciekać z wyspy .
Oblężenie Tulonu .
W lipcu 1793 , Bonaparte opublikował pro-republikański pamflet pt .
`` Le souper de Beaucaire '' ( Kolacja w Beaucaire ) , którym zjednał sobie wsparcie Augustyna Robespierre ’ a , młodszego brata lidera Rewolucji , Maksymiliana Robespierre ’ a .
Z pomocą swojego korsykańskiego znajomego , Antoine ’ a Christophe ’ a Salicetiego , Bonaparte został zaprzysiężony kapitanem artylerii sił rewolucyjnych podczas oblężenia Tulonu .
Zaadaptował plan obsadzenia artylerią wzgórza niedaleko miasta w celu dosięgnięcia nabrzeża miasta i zmuszenia do odwrotu Anglików , którzy bronili miasta z morza .
Atak z tej pozycji umożliwił zajęcie miasta , jednak w trakcie natarcia Napoleon został ranny w udo .
Dzięki temu sukcesowi został mianowany generałem brygady mając zaledwie 24 lata .
Będąc zauważonym przez Komitet Ocalenia Publicznego , został przydzielony do wojsk artyleryjskich w Armii Włoch .
Napoleon , czekając na potwierdzenie swoich apanaży z Armii Włoch , przeprowadzał inspekcje nadmorskich fortyfikacji niedaleko Marsylii .
Opracował także plany ataku na Królestwo Sardynii , jako część francuskiej odpowiedzi na działania Pierwszej Koalicji .
