Nardò
Nardò – miejscowość i gmina we Włoszech , w regionie Apulia , w prowincji Lecce .
Według danych na rok 2004 gminę zamieszkiwało 30 807 osób , 162,1 os./km² .
Tor .
W Nardò znajduje się tor używany do jazd próbnych samochodów osiągających wysokie prędkości .
Długość toru wynosi 12,5 km .
Jest on pochylony i idealnie okrągły , co powoduje , że kierowcy nie używają praktycznie kierownicy , gdyż samochód sam skręca .
Jedynie supersamochody wymagają korygowania jazdy kierownicą .
Rekord prędkości na torze osiągnął Bugatti Veyron Super Sport ( 435 km/h ) .
