Narkotyk
Narkotyk ( gr .
`` narkōtikós '' 'oszałamiający ' ) – potoczna nazwa niektórych substancji psychoaktywnych działających na ośrodkowy układ nerwowy .
Ze względu na poglądy społeczne , różnice kulturowe , stany prawne itp .
nie ma jednoznacznej definicji słowa narkotyk .
Narkotykami nazywa się :
W polskim prawodawstwie środkiem odurzającym nazywamy substancję pochodzenia naturalnego lub syntetycznego działającą na ośrodkowy układ nerwowy , określoną w rozporządzeniu wydanym na podstawie ustawy z dnia 29 lipca 2005 r. o przeciwdziałaniu narkomanii ( ) .
Zawiera ono też wykaz substancji psychotropowych .
Istnieje także definicja środka zastępczego :
Społeczne aspekty narkotyków .
Istnieje szereg substancji przyjmowanych przez ludzi , które wywołują zaburzenia percepcji lub uzależnienie , jednak za przyzwoleniem społecznym nie są one uważane za narkotyki .
W różnych kulturach podział ten wygląda odmiennie .
Polska należy do kręgu kulturowego , w którym narkotykami nie są nazywane następujące substancje ( pomimo udowodnionej szkodliwości i potencjału uzależniającego znacznie przewyższających wiele nielegalnych substancji – dotyczy to zwłaszcza nikotyny i alkoholu ) :
Ponadto wiele substancji psychoaktywnych jest syntetyzowanych przez organizm ludzki , co jest zupełnie naturalnym zjawiskiem .
