Naruhito
– 126. cesarz Japonii od 1 maja 2019 , książę w latach 1989–2019 jako następca tronu ( `` kōtaishi '' ) Japonii .
Sukcesja tronu .
8 sierpnia 2016 cesarz Akihito wygłosił orędzie do narodu , wyrażając w nim swoją wolę abdykacji ze względu na zły stan zdrowia i podeszły wiek .
Doszło do niej 30 kwietnia 2019 po uzgodnieniach pomiędzy : Dworem Cesarskim , Agencją Dworu Cesarskiego i rządem .
Następnego dnia odbyła się pierwsza z głównych ceremonii , w długim procesie intronizacji , polegająca na przekazaniu regaliów cesarskich jego starszemu synowi , księciu Naruhito , który tym samym został kolejnym , 126. cesarzem Japonii , rozpoczynając przy tym erę Reiwa .
22 października 2019 cesarz Naruhito formalnie ogłosił swoją intronizację oraz przyjął gratulacje od przedstawicieli narodu japońskiego i licznych gości zagranicznych .
Główna ceremonia intronizacji o nazwie „ Sokuirei-Seiden-no-gi ” odbyła się w sali Seiden-Matsu-no-Ma w Pałacu Cesarskim w Tokio .
W Japonii , gdy mówi się o cesarzu , nie używa się jego imienia , ale tytułu .
Życiorys .
Urodził się jako najstarszy syn księcia Akihito , następcy tronu , oraz księżnej Michiko .
W 1989 roku jego ojciec objął tron po śmierci swego ojca cesarza Hirohito , a Naruhito został następcą tronu .
