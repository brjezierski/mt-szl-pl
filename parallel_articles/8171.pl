Narwa ( Białoruś )
Narwa – dawna kolonia na Białorusi , w obwodzie brzeskim , w rejonie prużańskim , w sielsowiecie Suchopol .
W latach 1921–1939 należała do gminy Suchopol .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwały 54 osoby , wszystkie były wyznania prawosławnego i zadeklarowały białoruską przynależność narodową .
Było tu 7 budynków mieszkalnych .
