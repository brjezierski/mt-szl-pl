Natalia O ’ Shea
Natalia Andriejewna O ’ Shea ( ) , panieńskie nazwisko Nikołajewa ( ; ur .
3 września 1976 ) - rosyjska piosenkarka , autorka tekstów , aranżerka i liderka grupy Mielnica .
Z wykształcenia filolog , specjalista od języków celtyckich .
Ukończyła studia na Wydziale Filologicznym Uniwersytetu Moskiewskiego , gdzie w 2003 obroniła rozprawę kandydacką i otrzymała tytuł kandydata nauk .
Posiada obywatelstwo rosyjskie i irlandzkie .
