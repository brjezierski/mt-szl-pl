Natchez ( Missisipi )
Natchez – miasto w Stanach Zjednoczonych , położone w stanie Missisipi , w Hrabstwie Adams nad rzeką Missisipi .
Liczy 14 615 mieszkańców ( 2019 ) .
Założone zostało w 1716 roku i było pierwszą stolicą tego stanu , jest też jednym z najstarszych miast .
Duży port morski dostępny dla statków rzecznych .
W mieście rozwinął się przemysł drzewny , gumowy oraz spożywczy
