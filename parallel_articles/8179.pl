Nathaniel Mayer
Nathaniel Mayer ( ur .
10 lutego 1944 w Detroit , zm .
1 listopada 2008 tamże ) – amerykański muzyk śpiewający piosenki rhythm and blues .
Swoją karierę rozpoczął na początku lat 60 .
XX wieku w wytwórni Fortune Records .
Znany był również pod pseudonimami „ Nay Dog ” lub „ Nate ” .
