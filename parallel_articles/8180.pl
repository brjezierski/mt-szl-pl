Liczby naturalne
Liczby naturalne – liczby służące podawaniu liczności ( `` trzy '' osoby , zob .
liczebnik główny/kardynalny ) i ustalania kolejności ( `` trzecia '' osoba , zob .
liczebnik porządkowy ) , poddane w matematyce dalszym uogólnieniom ( odpowiednio : liczby kardynalne , liczby porządkowe ) .
Badaniem własności liczb naturalnych zajmują się arytmetyka i teoria liczb .
W matematyce określenie `` liczby naturalne '' oznacza na ogół liczby całkowite dodatnie .
To , czy zero jest liczbą naturalną , jest kwestią umowy .
W matematyce nie przyjęto ogólnie żadnej konwencji dotyczącej przynależności zera lub jej braku do liczb naturalnych .
Interesujące , że z punktu widzenia matematyki obie definicje można uważać w gruncie rzeczy za równoważne .
O konkretnym stanowisku decydują często takie sytuacje jak : uproszczenie zapisu pewnych symboli , ograniczenie przypadków szczególnych itp .
Historia .
Pierwsze systematyczne , abstrakcyjne studia nad liczbami przypisuje się starożytnym Grekom : Pitagorasowi , Euklidesowi i Archimedesowi .
Poza Grecją niezależne rozważania prowadzono w rejonie Indii , Chin i Ameryki Środkowej .
Pierwszym krokiem do wyabstrahowania liczb naturalnych było stworzenie sposobu ich zapisu .
W Babilonii stosowano na przykład cyfry o wartościach od 1 do 10 , gdzie o wartości liczby decydowała pozycja kolejnych cyfr w szeregu .
W starożytnym Egipcie stosowano odpowiednie hieroglify o wartościach 1 , 10 i kolejnych potęgach 10 , aż do miliona .
Choć wydawałoby się , że liczby naturalne są podstawowym pojęciem matematycznym i ich definicja była jedną z wcześniejszych , to jednak jest inaczej .
