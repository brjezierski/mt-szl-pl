Nowogródek
Nowogródek ( , `` Nawahrudak '' ; lit .
Naugardukas ; , `` Nowogrudok '' ) – miasto na Białorusi , w obwodzie grodzieńskim , na Wysoczyźnie Nowogródzkiej , siedziba administracyjna rejonu nowogródzkiego .
W 2018 roku liczyło ok. 29,4 tys .
mieszkańców .
Stolica województwa nowogródzkiego I Rzeczypospolitej ( 1500–1795 ) , stolica województwa nowogródzkiego II Rzeczypospolitej .
Prawo magdeburskie nadał Zygmunt I Stary dnia 26 lipca 1511 roku .
Miejsce obrad sejmików ziemskich powiatu nowogródzkiego od XVI wieku do pierwszej połowy XVIII wieku .
Historia .
Średniowieczne początki .
Według badań archeologicznych Nowogródek został założony w drugiej połowie X w. Początkowo składał się z grodu , wzniesionego na Górze Zamkowej , i nieumocnionego podgrodzia , zlokalizowanego na sąsiedniej górze na zachód od Góry Zamkowej .
W XII wieku podgrodzie zostało otoczone wałem .
Domostwa o zrębowej konstrukcji wznoszone były z gliny , a ich okna w przypadku bogatszych właścicieli były szklone .
Mieszkańcy utrzymywali się z myślistwa i rolnictwa .
Rozwinęło się rzemiosło : obróbka żelaza , metali kolorowych , kamieni , kości i drewna .
Kupcy utrzymywali kontakty handlowe z Kijowem , Bizancjum i Syrią .
Przez kilka stuleci Nowogródek stanowił centrum osadnictwa w regionie , jednak nie miał znaczących wpływów politycznych .
Z tego powodu w źródłach pisanych nazwa miasta została odnotowana w połowie XIII w. w kontekście konfliktów książąt halicko-wołyńskich z litewskimi .
