Nawakszut
Nawakszut ( , `` Nawākšūṭ '' ; ) – stolica Mauretanii , położona w zachodniej części kraju , 4 km od wybrzeża Atlantyku .
Nawakszut jest również stolicą wydzielonego dystryktu stołecznego w Mauretanii .
W 2013 roku miasto liczyło ok. 958 tys .
