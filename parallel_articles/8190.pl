Nazaret
Nazaret ( , `` Nacerat '' ; , `` An-Nasira '' ) – największe miasto i stolica administracyjna Dystryktu Północnego w Izraelu .
Jest to największe arabskie miasto w kraju i dlatego często jest nazywane „ arabską stolicą Izraela ” .
Stanowi gospodarczą , polityczną , kulturalną i medialną stolicę izraelskich Arabów .
Nastroje panujące w mieście są ważnym wskaźnikiem nastrojów panujących w arabskiej społeczności Izraela .
W Ewangeliach Nazaret zostało przedstawione jako miejsce dorastania Jezusa Chrystusa , w związku z czym wybudowano tutaj liczne sanktuaria upamiętniające wydarzenia biblijne .
Stały się one celem pielgrzymek chrześcijan .
Położenie .
Miasto Nazaret jest położone w Dolnej Galilei , w północnej części Izraela , w odległości 25 km od Jeziora Tyberiadzkiego i 9 km na zachód od góry Tabor .
W jego otoczeniu znajduje się miasto Nof ha-Galil , miasteczka Ar-Rajna , Kefar Tawor , Iksal , Jafa an-Naserije i Ilut , moszawy Tel Adaszim i Cippori oraz kibuce Mizra , Ginnegar i Kefar ha-Choresz .
Nazaret wraz z przyległymi miejscowościami utworzył dużą aglomerację miejską .
Po stronie południowo-zachodniej jest miejscowość Jafa an-Naserije , na zachodzie miasto Nacerat Illit , a dalej w kierunku północno-wschodnim ciągną się miejscowości Ar-Rajna , Ein Mahil , Maszhad i Kefar Kanna .
Tworzą one zwarty , terytorialnie przyległy do siebie obszar jednej dużej aglomeracji miejskiej .
Utworzono tutaj obszar metropolitalny .
Wywiera on silny wpływ na położone w pobliżu miasta satelickie .
Jest to położone na południowym zachodzie miasto Migdal ha-Emek , na południowym wschodzie miejscowość Iksal , na wschodzie Kefar Tawor , na północnym wschodzie Tur'an i na zachodzie miejscowość Ilut .
Przez całą aglomerację przechodzą liczne drogi przelotowe .
Nieprzerwana długość obszaru zabudowanego wynosi 12 km , a maksymalna szerokość sięga 5 km .
