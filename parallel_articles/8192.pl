Nałęczów ( stacja kolejowa )
Nałęczów – stacja kolejowa położona u styku wsi Drzewce , Drzewce-Kolonia i Piotrowice , 4 km na północ od miasta Nałęczów , w województwie lubelskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Stacja styczna z nieczynną stacją Nałęczów Wąskotorowy Nadwiślańskiej Kolejki Wąskotorowej .
W roku 2017 stacja obsługiwała 200–299 pasażerów na dobę .
Na stacji znajduje się kasa , poczekalnia i kiosk .
W latach 2016–2019 układ torowy stacji oraz infrastruktura obsługi podróżnych , są przebudowywane w związku z modernizacją linii kolejowej nr 7 .
