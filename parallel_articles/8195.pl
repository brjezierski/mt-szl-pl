Nea Nikomidia
Nea Nikomidia , Nea Nikomedia ( gr .
`` Νέα Νικομήδεια '' ) – wieś w północnej Grecji , w administracji zdecentralizowanej Macedonia-Tracja , w regionie Macedonia Środkowa , w jednostce regionalnej Imatia , w gminie Weria .
W odległości ok. 2 km od wsi znajduje się stanowisko archeologiczne z ruinami osady neolitycznej .
Współczesna Nea Nikomidia .
Wieś pierwotnie nosiła nazwę Braniata ( Μπρανιάτα ) .
W 1922 roku przesiedlono do niej greckich uchodźców z anatolijskiej Nikomedii ; na pamiątkę tego wydarzenia w 1953 roku jej nazwę zmieniono na Nea Nikomidia .
