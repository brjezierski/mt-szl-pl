Archeologia .
Na początku lat 60 .
XX wieku zespół archeologów pod kierownictwem R.J. Roddena przeprowadził prace wykopaliskowe , w trakcie których odkryta została jedna z najstarszych znanych osad neolitycznych na obszarze Europy .
Datowanie radiowęglowe pozwoliło ustalić chronologię na 6200–5300 p.n.e .
Osada składała się z czworokątnych domów wznoszonych z gliny i trzciny .
W centrum znajdował się duży budynek , w którym znaleziono wykonane z terakoty figurki postaci kobiecych , pełniący prawdopodobnie funkcje sakralne .
Ludność zajmowała się uprawą roślin ( pszenica , jęczmień , soczewica ) oraz hodowlą zwierząt ( owce , kozy , świnie , bydło rogate ) .
W osadzie wyrabiano ceramikę zdobioną odciskami palców i ornamentami geometrycznymi oraz kamienne narzędzia .
Odkryto także kilka pochówków .
