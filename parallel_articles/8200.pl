Needham ( Massachusetts )
Needham – miasto ( `` city '' ) w hrabstwie Norfolk , w stanie Massachusetts , w Stanach Zjednoczonych , położone ok. 15 km na południowy zachód od Bostonu .
W 2010 roku miasto liczyło 28 886 mieszkańców .
W Needham swoją siedzibę ma uczelnia Franklin W. Olin College of Engineering .
Przez miasto przebiega autostrada międzystanowa nr 95 .
