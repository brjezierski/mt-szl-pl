Neil Armstrong
Neil Alden Armstrong ( ur .
5 sierpnia 1930 w Wapakoneta , zm .
25 sierpnia 2012 w Cincinnati ) – amerykański astronauta , dowódca misji Apollo 11 .
21 lipca 1969 ( według czasu UTC ) jako pierwszy człowiek stanął na Księżycu .
Wypowiedział słynne zdanie : .
Życiorys .
Urodził się 5 sierpnia 1930 na farmie w stanie Ohio .
Był najstarszym z trojga dzieci Stephena i Violi Armstrongów .
Miał siostrę June ( ur .
6 czerwca 1933 ) i brata Deana ( ur .
22 lutego 1935 ) .
Matka Neila nie pracowała .
Zajmowała się domem , a w niedzielę uczyła religii w szkółce niedzielnej .
Ojciec był urzędnikiem stanu Ohio .
Jego praca zmuszała rodzinę do częstych przeprowadzek .
Zanim Neil skończył trzynaście lat Armstrongowie przeprowadzili się szesnaście razy .
