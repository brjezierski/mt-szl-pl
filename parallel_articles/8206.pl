Catgirl
Catgirl – określenie osoby płci żeńskiej z kocimi uszami , kocim ogonem bądź innymi kocimi atrybutami .
Dziewczyny-koty są czasami spotykane w anime i mandze , a także w grach komputerowych .
Catgirl mogą nosić duże rękawiczki bądź buty imitujące łapy .
