Inne zwierzęce kombinacje , najczęściej ssaki jak bunnygirls ( dziewczyny-króliczki ) , foxgirls ( dziewczyny-lisy ) , cowgirls ( dziewczyny-krowy ) czy rzadziej doggirls ( dziewczyny-psy ) , określa się jako .
Wschodnie dziewczyny-koty przedstawiane są z pionowymi źrenicami , ogonami i uszami .
Ich zachodnie odpowiedniczki wyglądem bardziej przypominają zwierzęta , z całym ciałem pokrytym futrem i łapami zakończonymi pazurami .
