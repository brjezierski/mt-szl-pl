Nelson Mandela
Nelson Rolihlahla Mandela , wymowa xhosa : [ ] ( ur .
18 lipca 1918 w Mvezo , zm .
5 grudnia 2013 w Johannesburgu ) – południowoafrykański polityk , prezydent Republiki Południowej Afryki w latach 1994–1999 , jeden z przywódców ruchu przeciw apartheidowi , działacz na rzecz praw człowieka , laureat Pokojowej Nagrody Nobla .
W Republice Południowej Afryki nazywany również „ Madiba ” ( nazwa jednego z klanów ludu Xhosa ) , a także „ tata ” , co często rozpisywano jako : „ Ojciec Narodu ” .
Był zarówno najstarszym ( złożył urząd w wieku 81 lat ) , jak i najdłużej żyjącym prezydentem RPA ( zmarł mając 95 lat ) .
W 1942 roku wstąpił do Afrykańskiego Kongresu Narodowego ( ANC ) , a w 1950 roku zaczął nim kierować .
Początkowo był zwolennikiem nieużywania przemocy .
Wraz ze współpracownikami zmienił zdanie po masakrze w czasie protestu Sharpeville w marcu 1960 roku i delegalizacji ANC .
Rok później został przywódcą zbrojnego skrzydła ANC `` Umkhonto we Sizwe '' ( „ Włócznia Narodu ” , MK ) .
Zbrojne działania „ Umkhonto we Sizwe ” za czasów Mandeli miały mieć jednak charakter wyłącznie sabotażowy i wykluczały ataki na ludzi .
Dokonywali zniszczeń instalacji o dużym znaczeniu dla gospodarki RPA .
Został aresztowany 5 sierpnia 1962 i skazany za rzekome akty sabotażu i przygotowywanie inwazji obcych państw na RPA .
Osadzony w więzieniu na wyspie Robben Island .
W 1985 roku odmówił skorzystania ze zwolnienia warunkowego .
