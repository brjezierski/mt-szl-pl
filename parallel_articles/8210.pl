Nenagh
Nenagh ( irl .
Aonach Urmhumhan lub An tAonach ) – miasto w Irlandii , w hrabstwie Tipperary .
Populacja miasta w roku 2011 wynosiła 8 023 mieszkańców .
Irlandzka nazwa Aonach Urmhumhan ( ang .
The Fair of Ormond ) wskazuje na handlową historię miasta .
Nazwa miasta oznacza „ Targ w Ormond ” ( Ormond - nazwa Wschodniego Munsteru ) .
