Galaretówka przejrzysta
Galaretówka przejrzysta ( `` Neobulgaria pura '' ( Pers . )
Petr . )
– gatunek grzybów z rodziny `` Gelatinodiscaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neobulgaria , Gelatinodiscaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1796 r. Christian Hendrik Persoon nadając mu nazwę `` Peziza pura '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1921 r. Franz Petrak , przenosząc go do rodzaju `` Neobulgaria '' .
Synonimy naukowe :
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda .
Morfologia .
Typu apotecjum , o konsystencji wodnistej , galaretowatej .
Podczas wysychania kurczy się .
Ma średnicę 1–3 cm , barwę bladoczerwono-brązową lub liliowoszarą .
Za młodu jest okrągławy , potem rozpostarty i nieregularnie tarczowaty lub kubkowaty z wystającym słabo karbowanym brzegiem .
