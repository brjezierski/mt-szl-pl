Neoerysiphe galeopsidis
Neoerysiphe galeopsidis ( DC . )
U. Braun – gatunek grzybów z rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny niektórych gatunków roślin .
Wywołuje u nich chorobę o nazwie mączniak prawdziwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neoerysiphe , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1815 r. Augustin Pyramus de Candolle nadając mu nazwę `` Erysiphe galeopsidis '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Uwe Braun w 1999 r .
Synonimy :
Morfologia .
Początkowo tworzy na obu stronach liści i na łodygach roślin cienkie , białe grzybnie .
