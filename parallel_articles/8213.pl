Neofabraea malicorticis
Neofabraea malicorticis H.S .
Jacks .
– gatunek grzybów z rodziny `` Dermateaceae '' .
Pasożyt , wraz z gatunkiem `` Neofabraea vagabunda '' wywołujący zgorzel kory jabłoni i gorzką zgniliznę jabłek .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neofabraea , Dermateaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Grzyb mikroskopijny , saprotrof i pasożyt .
Znana jest głównie jego anamorfa , teleomorfa występuje bardzo rzadko i nie odgrywa roli w zakażaniu drzew .
Grzybnia hodowana na agarowej pożywce po 14 dniach osiąga średnicę 6 cm .
Składa się z grzybni powietrznej złożonej z białych strzępek .
Zarodniki konidialne wytwarzane są w kubeczkowatych acerwulusach zbudowanych z małych , izodiametrycznych komórek o średnicy 4–8 μm i jasnożółtej barwie .
Po 2-4 tygodniach formowane są w nich bezbarwne konidiofory z fialidami .
Fialidy o kształcie cylindrycznym do wąsko gruszkowatego , długości przeważnie 12–16 μm i grubości 2,5–3 μm .
Wytwarzane są w nich makrokonidia , mikrokonidia oraz zarodniki o wielkości pośredniej – te najczęściej .
Makrokonidia o bardzo zmiennym kształcie , zazwyczaj lekko lub silnie ( na kształt litery U ) zakrzywione , często z 1-3 przegrodami , bezbarwne , o rozmiarach 15–35 × 3–6 μm .
