Pantera sundajska
Pantera sundajska ( `` Neofelis diardi '' ) – gatunek ssaka z podrodziny panter ( Pantherinae ) w obrębie rodziny kotowatych ( Felidae ) .
Występuje na Sumatrze i Borneo .
Od swoich kontynentalnych krewniaków różni się umaszczeniem i rozłożeniem cętek na futrze .
Taksonomia .
Gatunek po raz pierwszy zgodnie z zasadami nazewnictwa binominalnego opisał w 1823 roku francuski przyrodnik Georges Cuvier , nadając mu nazwę `` Felis diardi '' .
Holotyp pochodził z Jawy .
Przez długi czas `` N. diardi '' traktowany był jako podgatunek `` N. nebulosa '' , zidentyfikowany w 2007 na podstawie badań DNA jako nowy gatunek , który oddzielił się od `` N. nebulosa '' 1,4 miliona lat temu .
Na podstawie analizy genetycznej wyróżniono dwa podgatunki `` N. diardi '' .
