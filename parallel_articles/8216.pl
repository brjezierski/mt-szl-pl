Twardoskórzak lepki
Twardoskórzak lepki , twardziak lepki ( `` Neolentinus adhaerens '' ( Alb .
& amp ; Schwein . )
Redhead & amp ; Ginns ) – gatunek grzybów z rodziny niszczycowatych ( `` Gloeophyllaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neolentinus , Gloeophyllaceae , Gloeophyllales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1805 r. Johannes Baptista von Albertini i Lewis Schweinitz , nadając mu nazwę `` Agaricus adhaerens '' .
Obecną , uznaną przez Index Fungorum nazwę , nadali mu w 1985 r. Redhead i Ginns , przenosząc go do rodzaju `` Neolentinus '' .
Synonimów naukowych ma ponad 20 .
Niektóre z nich :
W 1983 r. Barbara Gumińska i Władysław Wojewoda w 1983 r. podali polską nazwę twardziak lepki .
Wówczas gatunek ten klasyfikowany był do rodzaju `` Lentinus '' ( twardziak ) .
Wcześniej ( w 1936 r. ) Teodorowicz opisywał ten gatunek pod nazwą łyczak przylgnięty .
Po przeniesieniu go do rodzaju `` Neolentinus '' obydwie polskie nazwy stały się niespójne z nazwą naukową .
