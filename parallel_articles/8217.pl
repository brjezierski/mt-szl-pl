Neonectria ditissima ( Tul .
& amp ; C .
Tul . )
Samuels & amp ; Rossman – gatunek grzybów z rodziny gruzełkowatych ( `` Nectriaceae '' ) .
Pasożyt wielu drzew , wywołujący chorobę , której sadownicy nadali nazwę rak drzew owocowych i zgnilizna owoców , a leśnicy rak gruzełkowy drzew liściastych
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neonectria , Nectriaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1865 r. Louis René Tulasne i Charles Tulasne nadając mu nazwę `` Nectria ditissima '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2006 r. Samuels i Rossman .
Niektóre synonimy :
Jeszcze do niedawna jako odrębny gatunek opisywano `` Cylindrocarpon heteronema '' .
Obecnie wiadomo już , że jest to tylko bezpłciowe stadium ( anamorfa ) `` Neonectria ditissima '' .
Morfologia .
W zaatakowanych przez patogena tkankach roślin rozwija się grzybnia powodująca zrakowacenia pędów .
