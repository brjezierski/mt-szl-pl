Historia .
Pierwszymi grupami ludności zamieszkującej kraj były plemiona indyjskie i tybetańskie .
W trzecim wieku p.n.e .
kraj podbił Aśoka z dynastii indyjskich Maurjów .
Po upadku Maurjów kraj znalazł się w stanie zależności od sąsiadów , a w szczególności Tybetu .
Od wieku XIII władze sprawowała miejscowa Malla ( dynastia ) , której najwybitniejszym przedstawicielem był Dźajasthiti .
W okresie rządów dynastii wprowadzono surowo przestrzegany podział kastowy .
