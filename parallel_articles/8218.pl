Geografia .
Ponad 80 % powierzchni kraju pokrywają góry o średniej wysokości ok. 6000 m n.p.m .
W północnej części panuje klimat górski , a w południowej zwrotnikowy monsunowy .
Południową część kraju zajmują wyżyny i tereny pagórkowate pokryte dżunglą oraz północny skraj Niziny Hindustańskiej ( tzw .
