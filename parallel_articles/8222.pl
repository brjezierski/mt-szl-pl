Nes ( gmina )
Nes ( far .
`` Nes kommuna '' ) – jedna z 30 gmin archipelagu Wysp Owczych , duńskiego terytorium zależnego na Oceanie Atlantyckim .
Do Nes kommuny przylega jedynie gmina Runavík .
Siedzibą władz gminy jest Toftir .
Gmina znajduje się w południowej części wyspy Eysturoy .
Zajmuje powierzchnię 13,9 km² .
Według danych na 1 stycznia 2014 roku zamieszkuje ją 1212 osób .
Historia .
Od 1872 roku istniała Eysturoyar Prestagjalds kommuna , w której skład wchodziły tereny współczesnej gminy Nes .
Od tej jednostki w 1894 roku odłączyła się Nes og Gøtu kommuna , z tej zaś osiemnaście lat później oddzieliła się gmina Nes .
W 1967 odłączyła się od niej gmina Runavík .
