Neuenbürg ( powiat Enz )
Neuenbürg – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Karlsruhe , w regionie Nordschwarzwald , w powiecie Enz , siedziba wspólnoty administracyjnej Neuenbürg .
Leży nad rzeką Enz , ok. 10 km na południowy zachód od Pforzheim , przy drodze krajowej B294 .
