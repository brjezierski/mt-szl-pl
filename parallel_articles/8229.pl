Neuilly-sur-Seine
Neuilly-sur-Seine ( wym .
[ ] ) – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Według danych na rok 2012 gminę zamieszkiwało 62 021 osób , a gęstość zaludnienia wynosiła 16 628 osób/km² .
Wśród 1287 gmin regionu Île-de-France Neuilly-sur-Seine plasuje się na 771. miejscu pod względem powierzchni .
