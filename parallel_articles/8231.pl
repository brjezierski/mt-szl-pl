Neurospora crassa
Neurospora crassa – gatunek grzybów z rzędu `` Sordariales '' , typ czerwonej pleśni z gromady workowców ( `` Ascomycota '' ) , tworzącej się głównie na chlebie , występujący również w terenach podmokłych i tropikalnych .
Grecka nazwa oznacza `` grzyb nerwowy '' i odnosi się do charakterystycznego prążkowania strzępek .
Pierwsze opublikowane doniesienia o tym gatunku , pochodzą z zarażenia w jednej z francuskich piekarni w 1843 roku .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Neurospora , Sordariaceae , Sordariales , Sordariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten opisali Cornelius Lott Shear i Bernard Ogilvie Dodge w 1927 r. na miąższu trzciny cukrowej .
Znaczenie .
`` N. crassa '' jest wykorzystywana w biologii jako organizm modelowy .
Edward Lawrie Tatum i George Wells Beadle wykorzystali promieniowanie rentgenowskie do mutagenezy `` N. crassa '' i wykazali , że uzyskane w ten sposób mutacje powodowały zmiany w funkcjonowaniu enzymów zaangażowanych w metabolizm .
Na podstawie tych badań sformułowali hipotezę `` jeden gen = jeden enzym '' .
W 1958 r. za swoje badania otrzymali Nagrodę Nobla w dziedzinie fizjologii i medycyny .
W 2003 poznano cały genom tego organizmu .
Ma on ok. 43 miliony par zasad i zawiera ok. 10000 genów na 7 chromosomach .
Realizowany jest projekt badający efekty uszkodzenia po kolei każdego genu tego organizmu .
