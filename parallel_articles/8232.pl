Neuss
Neuss ( lim .
`` Nüss '' ; do 1968 Neuß ) – miasto w zachodnich Niemczech w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Düsseldorf , w powiecie Rhein-Kreis Neuss , leżące na lewym brzegu Renu , niedaleko Düsseldorfu .
Neuss jest największym miastem powiatu , znane przede wszystkim ze swojej rzymskiej przeszłości , portu nad Renem i obywatelskiego festynu strzeleckiego .
