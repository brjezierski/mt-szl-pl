Historia .
Około 1524 roku do wybrzeży New Jersey dotarł , pozostający w służbie króla Francji , podróżnik włoski Giovanni da Verrazzano .
W 1609 roku dolny bieg rzeki , nazwanej później jego imieniem , badał Anglik Henry Hudson .
Od ok. 1620 roku obszar dzisiejszego New Jersey zasiedlali Holendrzy i Szwedzi .
W 1625 roku tereny te weszły w skład kolonii Nowa Szwecja , przyłączonej w 1655 roku do Nowych Niderlandów .
W 1664 roku zajęte przez Brytyjczyków , weszły w skład kolonii Nowy Jork , zostały jednak wkrótce wydzielone administracyjnie jako odrębna kolonia ( Prowincja New Jersey ) .
W 1776 roku New Jersey proklamowało niezależność od Anglii .
W 1787 roku prowincja była jednym ze stanów-założycieli Unii .
