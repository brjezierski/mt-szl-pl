New Lexington
New Lexington – wieś w USA , w stanie Ohio , siedziba władz hrabstwa Perry .
Liczba mieszkańców w 2000 roku wynosiła 4423 .
Klimat .
Wieś leży w strefie klimatu umiarkowanego , ciepłego , subtropikalnego , bez pory suchej z gorącym latem , należącego według klasyfikacji Köppena do strefy `` Cfa '' .
Średnia temperatura roczna wynosi 10,7°C , a opady 1021,1 mm ( w tym do 49 cm opadów śniegu ) .
Średnia temperatura najcieplejszego miesiąca - lipca wynosi 22,4°C , najzimniejszego - stycznia -2,2°C podczas gdy rekordowe temperatury wynoszą odpowiednio 39,4°C i -37,2°C .
