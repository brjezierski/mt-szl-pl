New Rochelle
New Rochelle – miasto w Stanach Zjednoczonych , w stanie Nowy Jork , nad zatoką Long Island Sound ( Ocean Atlantycki ) .
Nazwa New Rochelle pochodzi od francuskiego miasta `` La Rochelle '' .
Nazwa ta wzięła się od hugenotów szukających schronienia w XVII wieku na tych terenach .
Liczba mieszkańców : ok. 78,5 tys .
( siódme miasto w stanie Nowy Jork )
