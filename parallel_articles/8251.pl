Newcastle upon Tyne
Newcastle upon Tyne , City of Newcastle upon Tyne ( łac .
`` Novum Castellum super Tinam '' ) – miasto i dystrykt metropolitalny w północno-wschodniej Anglii ( Wielka Brytania ) , w hrabstwie ceremonialnym Tyne and Wear , położone na północnym brzegu rzeki Tyne , nieopodal jej ujścia do Morza Północnego .
Wchodzi w skład aglomeracji Tyneside .
Według spisu powszechnego z 2011 roku miasto liczyło 280 177 mieszkańców .
Historia .
Badania archeologiczne udowodniły istnienie prehistorycznych osad ludzkich przy ujściu rzeki Tyne , jednak historia Newcastle znana jest od czasów panowania cesarza Hadriana .
