Newnan
Newnan – miasto w Stanach Zjednoczonych , w stanie Georgia , siedziba administracyjna hrabstwa Coweta .
Należy do obszaru metropolitalnego Atlanty .
Według spisu w 2020 roku liczy 42,5 tys .
mieszkańców , w tym 34 % stanowili Afroamerykanie .
We wczesnych godzinach porannych 26 marca 2021 w miasto uderzyło gwałtowne tornado o sile EF4 , które spowodowało znaczne uszkodzenia strukturalne i pośrednio zabiło jedną osobę .
