Newton Mearns
Newton Mearns ( gael .
`` Baile Ùr na Maoirne '' ) – miasto w środkowej Szkocji , w hrabstwie East Renfrewshire , położone na południowo-zachodnim obrzeżu aglomeracji Glasgow .
W 2011 roku liczyło 24 234 mieszkańców .
W 1887 roku Newton Mearns było niewielką wsią , liczącą 900 mieszkańców .
Miejscowość rozrosła się w XX wieku i stanowi współcześnie zamożne , mieszkalne przedmieście Glasgow .
We wschodniej części miasta znajduje się XV-wieczna wieża rycerska Mearns Castle .
