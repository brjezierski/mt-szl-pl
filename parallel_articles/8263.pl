Neymar
Neymar , właśc .
Neymar da Silva Santos Júnior ( ur .
5 lutego 1992 w Mogi das Cruzes ) – brazylijski piłkarz , występujący na pozycji napastnika we francuskim klubie Paris Saint-Germain oraz w reprezentacji Brazylii .
Karierę seniorską rozpoczął w Santosie FC , w którym zadebiutował w 2009 .
W ciągu pięcioletniego pobytu w brazylijskim klubie dwukrotnie zdobył Campeonato Paulista , raz Copa do Brasil i Copa Libertadores .
W 2013 przeniósł się do FC Barcelony , z którą zdobył potrójną koronę w sezonie 2014/2015 , wygrywając Primera División , Copa del Rey i rozgrywki Ligi Mistrzów UEFA .
W 2017 , za rekordową kwotę , został zakupiony przez Paris Saint-Germain .
Z paryskim klubem cztery razy zdobywał mistrzostwo Francji , trzy razy Puchar Francji , dwa razy Puchar Ligi Francuskiej i trzy razy Superpuchar Francji , a w sezonie 2019/2020 został finalistą Ligi Mistrzów UEFA .
W reprezentacji Brazylii występuje od 2010 , a z 124 meczami i 77 golami jest najskuteczniejszym piłkarzem , razem z Pelé .
Srebrny medalista Igrzysk Olimpijskich 2012 .
W 2013 Brazylia wygrała Puchar Konfederacji , a Neymar dostał Złotą Piłkę dla mvp turnieju .
