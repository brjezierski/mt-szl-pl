Niagara Falls ( Kanada )
Niagara Falls – miasto w Kanadzie , w prowincji Ontario , nad rzeką Niagara , która tworzy granicę między Kanadą i USA .
Po drugiej stronie rzeki znajduje się amerykańskie miasto o tej samej nazwie w stanie Nowy Jork .
Wodospad Niagara , leżący niedaleko centrum miasta , przyciąga co roku
setki tysięcy turystów z całego świata .
Liczba mieszkańców Niagara Falls w 2006 roku wynosiła 82 000 .
Język angielski jest językiem ojczystym dla 78,2 % , francuski dla 2,5 % mieszkańców ( 2006 ) .
W mieście rozwinął się przemysł chemiczny , spożywczy , metalowy oraz szklarski .
