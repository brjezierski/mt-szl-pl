Niagara-on-the-Lake
Niagara-on-the-Lake – miasto ( ang .
`` town '' ) w Kanadzie , w prowincji Ontario , w regionie Niagara .
Miasto jest położone w rogu uformowanym przez jezioro Ontario i wpływającą do niego rzekę Niagara , która tworzy granicę między Kanadą i USA .
Po drugiej , amerykańskiej stronie rzeki jest zabytkowy Fort Niagara .
Wodospad Niagara leży około 20 km na południe od miasta .
