Dzieciństwo i młodość .
Machiavelli urodził się 3 maja 1469 w Florencji w rodzinie z tradycjami politycznymi , ale nie należącej do lokalnych elit politycznych i majątkowych ( `` ottimati '' ) .
Jego rodzicami byli Bernard , doktor prawa , oraz Bartolomea Nelli .
Chociaż jego dochody nie były znaczne , dysponował obszerną biblioteką , a w jego domu wysoko ceniono naukę .
Niccolo odebrał staranne wykształcenie humanistyczne .
Czytał licznych autorów starożytnych w łacińskich oryginałach lub tłumaczeniach ( greki nie znał ) .
