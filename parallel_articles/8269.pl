Życiorys .
Życie Machiavellego ściśle związane było z historycznymi wydarzeniami i politycznym kontekstem jego czasów .
Rozwojowi kultury renesansowej towarzyszyło znaczne rozbicie i niestabilność polityczna Włoch .
Florencja , pod przywództwem Medyceuszy była jednym z najważniejszych miast włoskich , pod względem politycznym , gospodarczym i kulturowym .
