Niccolò Zucchi
Niccolò Zucchi ( ur .
6 grudnia 1586 w Parmie , zm .
21 maja 1670 w Rzymie ) – włoski duchowny katolicki i naukowiec ; jezuita , astronom i fizyk .
Według niektórych źródeł był pierwszym astronomem , który zaobserwował pasy na Jowiszu , miał tego dokonać 17 maja 1630 .
Czasami jednak odkrycie to przypisuje się Francesco Fontanie lub Evangeliście Torricelliemu .
Zucchi jest również znany jako autor pierwszego opisu konstrukcji teleskopu zwierciadlanego , którego wynalezienie przypisuje się jednak powszechnie Newtonowi .
Jego najważniejszą pracą była `` „ Optica philosophia experimentis et ratione a fundamentis constituta ” '' , oprócz niej napisał również dwa obszerne dzieła poświęcone mechanice i konstrukcji maszyn .
Życiorys .
Zucchi pochodził z Parmy a jego rodzice , Pierre Zucchi i Francoise Giande Marie , należeli do miejscowej arystokracji .
Miał siedmioro rodzeństwa .
Edukację rozpoczął od kolegium jezuickiego w Parmie .
Później przebywał w Piacenzy , gdzie uczył się retoryki , po czym ponownie wrócił do Parmy aby studiować teologię .
W 1602 roku wstąpił do zakonu jezuitów i po zakończeniu nowicjatu pracował jako nauczyciel matematyki , teologii i retoryki w Rzymie i Rawennie .
Później otrzymał tytuł kaznodziei papieskiego i zajmował się opieką nad siedzibą jezuitów w Rzymie .
Był ceniony jako naukowiec i miał kilku możnych patronów , którzy przeznaczali część swoich pieniędzy na jego badania .
Z dedykacji zamieszczonych w pracach Zucchiego wynika , że jego mecenasami byli między innymi Ranuccio II Farnese , książę Parmy i Piacenzy oraz arcyksiążę Leopold , późniejszy cesarz .
Zucchi interesował się przede wszystkim naukami ścisłymi , głównie astronomią , maszynami i matematyką .
Interesowały go zwłaszcza tematu związane z magnetyzmem i konstrukcją barometrów oraz innych urządzeń i maszyn .
Udowodnił również , że luminofory wydzielają światło , a nie je skupiają .
Jego największą pasją była jednak astronomia .
Już w 1616 roku skonstruował i opisał jeden z pierwszych w historii , teleskopów zwierciadlanych .
Miał on jednak dość prymitywną budowę i nie zapewniał zbyt dobrego obrazu , dopiero innowacje Isaaca Newtona usprawniły to urządzenie .
Mimo to Zucchi z powodzeniem wykorzystywał go do obserwowania powierzchni Marsa , co zaowocowało dokładnym jej opisem , stworzonym w 1620 roku .
W 1623 roku Zucchi , jako legat papieski , brał udział w poselstwie na dwór cesarza Ferdynanda II .
Tam spotkał matematyka i astronoma , Johannesa Keplera , który pełnił wówczas funkcję nadwornego matematyka cesarskiego .
Po powrocie do Włoch stale korespondował z Keplerem na tematy dotyczące ich badań .
Wysłał mu również skonstruowany przez siebie teleskop .
W 1630 roku Zucchi wraz z innym jezuitą , Bartolim , jako pierwsi lub jedni z pierwszych astronomów , zaobserwowali tak zwane pasy na Jowiszu .
Na cześć Zucchiego jeden z kraterów na Księżycu nosi nazwę Zucchius .
