Mikołaj Henel
Mikołaj Henel ( , właściwie Nicolaus Henel von Hennenfeld , również Mikołaj Henel prudnicki ; ur .
11 stycznia 1582 w Prudniku , zm .
23 lipca 1656 we Wrocławiu ) – śląski regionalista , syndyk , biograf , kronikarz i historyk .
Uznawany jest za najwybitniejszego przedstawiciela renesansowej historiografii śląskiej .
Życiorys .
Urodził się 11 stycznia 1582 roku w Prudniku jako jedenaste dziecko Stefana Heneliusa ( 1546–1602 ) , miejscowego kaznodziei , a następnie pastora ewangelickiego kościoła w Rudziczce i jego żony Anny Kühne ( zm .
1596 ) , córki pisarza miejskiego w Prudniku .
Ich dom rodzinny znajdował się w południowej pierzei Rynku w Prudniku .
Mikołaj rozpoczął naukę w gimnazjum o profilu klasycznym w Prudniku , gdzie przykładano dużą wagę do nauczania literatury starożytnej oraz znajomości języka greckiego i łaciny .
Był pilnym uczniem , dzięki czemu stał się ulubieńcem nauczyciela Caspara Neandra .
Swego pierwszego nauczyciela wspominał ciepło i podkreślał , że właśnie dzięki niemu zainteresował się greką , łaciną oraz poezją .
Po wyjeździe Neandra do Kłodzka w 1594 , Henel przeniósł się do szkoły w Opawie , gdzie jego nauczycielem logiki był Daniel Staude .
Od 1596 uczył się w Gimnazjum św .
Elżbiety we Wrocławiu , gdzie wówczas rektorem był Nicolaus Steinberg .
Większą część roku 1598 chorował , a w 1599 zamknięto gimnazjum z powodu zarazy .
Wtedy ojciec polecił Mikołajowi udanie się na studia .
Zapisał się na Uniwersytet w Lipsku w zimowym semestrze 1599 roku .
