Język niderlandzki
Język niderlandzki ( niderl .
) , język holenderski , język flamandzki – język indoeuropejski z gałęzi zachodniej w ramach języków germańskich .
Dla 23 mln ludzi jest językiem ojczystym ( pierwszym ) , a dla kolejnych 5 mln drugim językiem .
Większość użytkowników tego języka mieszka na zachodzie Europy .
Niderlandzki objęty jest oficjalnym statusem języka urzędowego w Holandii i Belgii ( w Regionie Flamandzkim i Regionie Stołecznym Brukseli ) , a poza Europą w Surinamie i na dawnych Antylach Holenderskich , czyli Bonaire , Curaçao , Sabie , Sint Eustatius , Sint Maarten i Arubie .
