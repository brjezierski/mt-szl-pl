Niska
Niska ( , wym .
, ) – miasto w Niemczech , na Łużycach Górnych , we wschodniej części kraju związkowego Saksonia , w okręgu administracyjnym Drezno , w powiecie Görlitz , do 31 lipca 2008 stolica powiatu Niederschlesischer Oberlausitzkreis .
Miasto liczy 9526 mieszkańców ( 2014 ) , a jego powierzchnia wynosi 53,6 km² .
Historia .
Miasto założone w 1742 przez imigrantów z Czech , członków wywodzącego się z husytyzmu ruchu religijnego braci morawskich , prześladowanego w katolickiej ojczyźnie .
Nazwa `` Niesky '' jest zniemczeniem czeskiego słowa `` nízký '' ( pol .
`` niski '' ) .
Do 1763 roku w granicach unijnego państwa polsko-saskiego .
Od 1806 część Królestwa Saksonii , połączonego w latach 1807–1815 unią z Księstwem Warszawskim .
Po kongresie wiedeńskim w 1815 znalazło się w granicach Prowincji Śląsk Królestwa Prus .
W połowie XIX wieku do szkoły w Niskiej uczęszczał późniejszy polski pastor ewangelicki Ernest Wilhelm Bursche .
W 1871 Niska została częścią zjednoczonych Niemiec .
W latach 1919–1938 i 1941–1945 w granicach Prowincji Dolny Śląsk , a w latach 1938–1941 w granicach ponownie scalonej Prowincji Śląsk .
W trakcie II wojny światowej w miejscowości znajdowała się filia obozu koncentracyjnego Groß-Rosen .
W 1945 roku miejsce walk 2 Armii Wojska Polskiego ( Bitwa o Niesky ) .
Wieczorem dnia 17 kwietnia 1945 roku jednostki polskiego 1 Korpusu Pancernego i 9 Dywizji Piechoty podjęły nieudaną próbę zdobycia miasta .
Następnego dnia miejscowość otoczono i około godziny 18 zdobyto .
W tym samym dniu na południe od Niskiej zgrupowanie uderzeniowe niemieckiej Grupy Armii „ Środek ” zaatakowało lewe skrzydło 2 Armii WP .
20 kwietnia do walki wprowadzono dwa korpusy radzieckiej 52 Armii , które przejęły obronę od polskich wojsk .
Niemcy , napotkawszy silną obronę , podjęli operacje zaczepne bardziej na zachód , w rejonie Budziszyna .
Po II wojnie światowej decyzją Radzieckiej Administracji Wojskowej w Niemczech Niska wraz z położoną na zachód od Nysy Łużyckiej częścią Prowincji Dolny Śląsk została przyłączona do Saksonii .
W latach 1949–1990 w granicach NRD .
