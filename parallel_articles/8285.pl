Nightwish
Nightwish – fińska grupa założona w 1996 roku .
Styl grupy wyróżniał żeński wokal w postaci operowego głosu Tarji Turunen ( pięć pierwszych albumów ) , a następnie Anette Olzon ( albumy szósty i siódmy ) .
Od 2013 wokalistką zespołu jest Holenderka Floor Jansen , która nagrywa albumy grupy począwszy od ósmego .
Muzyka grupy składa się z połączenia gitarowych riffów z orkiestracjami .
Początkowo tworzonymi za pomocą instrumentów klawiszowych , od albumu `` Century Child '' ( 2002 ) przez orkiestrę symfoniczną .
Od wielu lat integralnym elementem w stylu zespołu są także różnorodne chóry oraz okazjonalnie instrumentarium folkowe .
Lider grupy , Tuomas Holopainen , określa gatunek zespołu jako metal symfoniczny .
Jest on też autorem większości poetyckich tekstów i kompozycji .
