Nikopol ( stacja kolejowa )
Nikopol ( ukr : `` Станція Нікополь '' ) – stacja kolejowa w Nikopolu , w obwodzie dniepropetrowskim , na Ukrainie .
Jest częścią administracji Kolei Naddnieprzańskiej .
Znajduje się na linii Apostołowo – Zaporoże , między stacjami Nikopolbud ( 10 km ) i Marganec ( 20 km ) .
W roku 1948 , stacja została zelektryfikowana napięciem prądu stałego ( 3 kV ) .
W budynku stacyjnym znajduje się poczekalnia , kasy biletowe i przechowalnia bagażu .
