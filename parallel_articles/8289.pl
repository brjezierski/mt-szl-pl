Lates nilowy
Lates nilowy ( `` Lates niloticus '' ) , bardziej znany pod handlowymi nazwami okoń nilowy lub okoń Wiktorii – gatunek słodkowodnej ryby okoniokształtnej z rodziny Latidae , wcześniej uznawanej za podrodzinę Latinea w rodzinie żuchwikowatych ( Centropomidae ) .
Występowanie .
Występuje powszechnie w krainie etiopskiej .
Gatunek rodzimy dla jezior Czad i Turkana , rzek : Kongo , Wolta , Nil , Senegal i in .
Występuje także w półsłonych wodach jeziora Marjut w Egipcie .
Pierwotnie zaliczany do słonowodnych wargaczy pod nazwą `` Labrus niloticus '' , a także `` Centropomus niloticus '' .
Lokalne nazwy zwyczajowe : belona afrykańska , okoń Wiktorii ( myląca nazwa handlowa , bo gatunek ten nie jest rodzimy dla jeziora Wiktorii ) , a także wiele innych , w różnych językach afrykańskich .
Introdukcja do jeziora Wiktorii .
Introdukcja tego gatunku do jeziora Wiktorii jest najczęściej przytaczanym przykładem negatywnego wpływu gatunków inwazyjnych na ekosystem .
Okonia nilowego wprowadzono do jeziora Wiktorii we wschodniej Afryce w latach 50 .
XX w. w celach hodowlanych .
Wywołało to wymarcie kilkuset gatunków rodzimych i spowodowało spadek liczebności populacji wielu innych .
Początkowo ryba ta żywiła się narybkiem innych ryb , ale gdy ich pogłowie spadło , zaczęła polować na krewetki i małe ryby .
Introdukcja tego gatunku , choć wpłynęła niekorzystnie na ekosystem , to sprowadziła nad jezioro Wiktorii wiele przedsiębiorstw zajmujących się rybołówstwem .
W 2003 roku eksport okonia nilowego do Unii Europejskiej przyniósł im 169 milionów euro zysku .
Jednak nadmierna eksploatacja okonia spowodowała spadek jego liczebności .
Zaburzenie ekosystemu jeziora miało także negatywny socjoekonomiczny wpływ na żyjącą wokół niego społeczność : mieszkańcy porzucili tradycyjne zajęcia i związali się z przemysłem rybołówczym , którego przyszłość jest niepewna .
