Nintendo
– japońskie przedsiębiorstwo założone 23 września 1889 przez Fusajirō Yamauchiego , w celu produkowania kart hanafuda do japońskiej gry o tej samej nazwie .
W ciągu kilkudziesięciu lat przedsiębiorstwo zajęło się rynkiem gier konsolowych i stało się jednym z potentatów w tej dziedzinie .
Wśród najpopularniejszych konsol Nintendo można wymienić Game Boy , Super Nintendo Entertainment System , Nintendo DS , Wii czy Nintendo Switch .
Nintendo wydało także wiele wpływowych franczyz , m.in .
Mario , `` The Legend of Zelda , '' `` Star Fox oraz '' `` Pokémon '' .
Przedsiębiorstwo i jego pracownicy zostali nagrodzeni licznymi nagrodami , w tym Game Developers Choice Awards oraz British Academy Games Awards .
