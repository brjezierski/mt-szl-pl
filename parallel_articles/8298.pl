Nittedal ( stacja kolejowa )
Nittedal – stacja kolejowa w Nittedal , w regionie Oslo w Norwegii , jest oddalona od Oslo Sentralstasjon o 24,26 km .
Jest położona na wysokości 235,8 m n.p.m .
Ruch pasażerski .
Leży na linii Gjøvikbanen .
Jest elementem kolei aglomeracyjnej w Oslo - w systemie SKM ma numer 300 .
Obsługuje Oslo Sentralstasjon , Gjøvik .
Pociągi odjeżdżają w obu kierunkach co pół godziny ; Nie wszystkie pociągi zatrzymują się na każdej stacji .
Obsługa pasażerów .
Wiata , poczekalnia , automat biletowy , parking na 70 miejsc , parking rowerowy , kawiarnia , autobus , ułatwienia dla niepełnosprawnych , postój taksówek .. Odprawa podróżnych odbywa się w pociągu .
