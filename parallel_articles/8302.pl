Niwki ( wzgórze )
Niwki ( ok. 325 m ) – niewielkie wzgórze na Wyżynie Częstochowskiej , po wschodniej stronie miejscowości Olsztyn w województwie śląskim , w powiecie częstochowskim , w gminie Olsztyn .
Znajduje się w ciągu wzgórz po wschodniej stronie Olsztyna , pomiędzy wzgórzami Lipówki ( 351 m ) i Cegielnia ( 349 m ) .
Pokryte jest łąkami .
