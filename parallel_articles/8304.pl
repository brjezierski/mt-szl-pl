Niemysłowice
Niemysłowice ( ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Obniżenia Prudnickiego , będącego częścią Niziny Śląskiej .
Nieoficjalną częścią wsi jest Spalony Dwór .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 665 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 8 km od granicy z Czechami , w zachodniej części Obniżenia Prudnickiego , tuż przy granicy gminy Prudnik z gminą Lubrza .
Należy do Euroregionu Pradziad .
Leży na wysokości 255–296 m n.p.m. , na obszarze 524,10 ha .
Ma charakter rolniczy .
724 mieszkańców zajmuje 55 gospodarstw .
Istnieją dwa duże gospodarstwa o powierzchni 50 ha , pozostałe liczą od 1 do 10 ha .
