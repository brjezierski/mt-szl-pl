Niesiebędowice
Niesiebędowice ( ) – wieś w Polsce położona w województwie opolskim , w powiecie nyskim , w gminie Korfantów .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Nazwa .
W kronice łacińskiej `` Liber fundationis episcopatus Vratislaviensis '' ( pol .
`` Księga uposażeń biskupstwa wrocławskiego '' ) spisanej w latach 1295-1305 miejscowość wymieniona jest w formie `` Nessebandowitz '' .
Według niemieckiego językoznawcy nazwa jest patronimiczną nazwą wywodzącą się od polskiego nazwiska - `` Nieszydowski '' lub `` Niesidowski '' i pochodziła od założyciela wsi .
W swoim dziele o nazwach miejscowych na Śląsku wydanym w 1888 roku we Wrocławiu jako starszą od niemieckiej wymienia polską formę nazwy - `` Nieszidowicz '' podając jej znaczenie `` Dorf der Nieszidowski '' - `` Wieś Nieszydowskiego '' .
Nazwa została zgermanizowana przez Niemców na `` Nussdorf '' w wyniku czego utraciła swoje pierwotne znaczenie .
