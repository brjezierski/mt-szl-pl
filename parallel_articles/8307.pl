Odbiór .
Na podstawie powieści ilustrowanej „ No Game No Life ” powstała manga oraz serial anime .
Redakcja polskojęzycznego serwisu tanuki.pl , wystawiła anime ocenę 7/10 .
W pierwszej połowie 2014 roku powieść sprzedała się w ponad 250 tys .
egzemplarzy , co uplasowało ją na dziewiątym miejscu , wśród najlepiej sprzedających się light novels .
