No Game No Life
– seria light noveli , napisanych i zilustrowanych przez Yū Kamiyę , wydawana przez Media Factory od 25 kwietnia 2012 roku .
Na jej podstawie powstała manga oraz anime .
Manga z ilustracjami Mashiro Hiiragiego wydawana jest od 27 stycznia 2013 roku w magazynie `` Monthly Comic Alive '' także publikowanym przez Media Factory .
Anime zostało wyprodukowane przez studio Madhouse .
Premierowa emisja miała miejsce od 9 kwietnia do 25 czerwca 2014 roku na antenie AT-X .
Fabuła .
Seria koncentruje się wokół rodzeństwa 18-letniego Sory i 11-letniej Shiro .
Są NEETami i hikikomori , którzy cały dzień spędzają na graniu w gry komputerowe , w których nie mają sobie równych .
W pewnym momencie , rozgrywają zwycięską partię szachów z Bogiem Tetem , który przenosi rodzeństwo do innego świata .
W świecie tym Sora i Shiro przynależą do najsłabszej spośród 16 ras i starają się zostać najsilniejszą z nich .
Powieści ilustrowane .
Tom jest zbiorem opowiadań ; tom ten został wydany w edycji zwykłej jak i limitowanej .
Do wersji limitowanej dołączono figurkę kolekcjonerską Sory .
Sprzedano łącznie ponad 4,5 miliona kopii powieści .
Manga .
W Polsce manga jest wydawana przez Waneko .
`` No Game No Life Zero '' .
