No pasarán
( ) – hasło używane dla podkreślenia determinacji utrzymania swej pozycji w walce , pierwszy raz użyte przez Dolores Ibárruri w czasie wojny domowej w Hiszpanii .
Stało się zawołaniem bojowym wszystkich antyfaszystów .
W pełnej wersji słowa Dolores Ibárruri Gómez brzmiały : `` ( „ Nie przejdą !
Lepiej umierać stojąc , niż żyć na klęczkach ! ” ) .
Od tego czasu powiedzenie to stało się najpopularniejszym hasłem używanym przez stronę republikańską i Brygady Międzynarodowe .
Gdy Madryt został zdobyty przez nacjonalistów , generał Francisco Franco w swym przemówieniu wygłoszonym po zdobyciu Madrytu 1 kwietnia 1939 stwierdził : ( „ przeszliśmy ” ) , co było odpowiedzią na hasło republikanów .
Jest to też tytuł wiersza Władysława Broniewskiego w którym poeta napisał :
„ No pasaran ” to też tytuł płyty i piosenki polskiego punkowego zespołu Blade Loki , Włochaty oraz jednego z utworów Jamal .
Pojawia się także w tytule piosenki zespołu rockowego T.Love oraz rosyjskiego rapera АК-47 .
