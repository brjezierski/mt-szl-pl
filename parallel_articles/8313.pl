Noemi ( piosenkarka )
Noemi , właściwie Veronica Scopelliti ( ur .
25 stycznia 1982 w Rzymie ) – włoska piosenkarka i kompozytorka .
Jej piosenki zaliczyć można do wielu gatunków : rocka , bluesa , soulu , popu , rhythm and bluesa , jazzu , reggae i ska .
Cechą charakterystyczną głosu artystki jest kontralt .
Jest córką Armando i Stefania .
Ma młodszą siostrę , Ariannę .
