Norbert Bonczyk
Norbert Bonczyk ( także Bonczek , Bończyk , Bontzek ; ur .
6 czerwca 1837 w Miechowicach , zm .
18 lutego 1893 w Bytomiu ) – ksiądz katolicki , poeta , autor artykułów do „ Zwiastuna Górnośląskiego ” , działacz narodowy na rzecz polskości Górnego Śląska , tłumacz liryki niemieckiej .
Zwany „ Śląskim Homerem ” .
Opisując obrazy z życia górnośląskiej społeczności wiejskiej , zwracał przede wszystkim uwagę na odrębność tych terenów w sensie historycznym , obyczajowym i częściowo językowym .
Wieloletni współpracownik księdza Józefa Szafranka .
Życiorys .
Pochodził ze zgermanizowanej rodziny górniczej .
Był synem sztygara kopalni cynku i ołowiu `` Maria '' w Miechowicach koło Bytomia .
W latach 1851-1858 uczęszczał do liceum w Gliwicach , po ukończeniu którego wstąpił na wydział teologii katolickiej Uniwersytetu Wrocławskiego .
W latach 1858-1862 studiował również filologię słowiańską u Wojciecha Cybulskiego i Wincentego Kraińskiego .
Był członkiem Towarzystwa Literacko-Słowiańskiego ( 1858-1860 ) oraz Towarzystwa Polskich Górnoślązaków .
Święcenia kapłańskie przyjął w 1862 i został wikarym w Piekarach Śląskich , a w 1865 w Bytomiu w parafii NMP , gdzie pracował do śmierci .
W 1874 objął obowiązki proboszcza w swej parafii , ale ze względu na negatywne nastawienie władz państwowych nie otrzymał wówczas oficjalnego tytułu proboszcza .
Tytuł ten dostał dopiero w 1886 .
Działalność .
W 1869 założył Towarzystwo Polsko-Katolickie tzw .
`` Kasyno '' , które skupiało wszystkie stany .
Prowadził w nim co niedziela odczyty i pogadanki w języku polskim .
Od czasów studenckich zaangażowany w działalność społeczną oraz w literacką .
We Wrocławiu działał w Towarzystwie Literacko-Słowiańskim , po powrocie na Górny Śląsk pisał do `` Zwiastuna Górnośląskiego '' .
W 1871 założył w Bytomiu Towarzystwo Św .
Alojzego , organizację społeczno kulturalną dla młodzieży , której celem było szerzenie kultury i oświaty na Śląsku .
Ułożył dla niej statut i był jej pierwszym prezesem .
Z jego inicjatywy zaczęły powstawać takie same towarzystwa prawie na całym Górnym Śląsku .
Był też działaczem katolickiej partii Centrum i współorganizatorem ruchu spółdzielczego na Górnym Śląsku .
Z jego inicjatywy powstały kościoły pw .
Św .
Trójcy i na wzgórzu Św .
Małgorzaty w Bytomiu .
Był również współogranizatorem i opiekunem dwóch katolickich związków robotniczych : `` Związku Wzajemnej Pomocy Robotników Górnośląskich '' ( 1889 ) i `` Towarzystwa Górnośląskich Przemysłowców . ''
Był przewodniczącym bytomskiego Komitetu Wyborczego , uczestniczył w wiecach polskich m.in .
w 1885 roku w Gliwicach .
W poglądach politycznych był zbliżony do niemieckiej partii Centrum , z którą współpracował .
Mocno zaangażowany w propagowanie języka polskiego , choć w okresie młodzieńczym pisał wiersze również po niemiecku i łacinie .
Oprócz tego przekładał literaturę niemiecką na polski , zwłaszcza Goethego i Schillera .
Za przetłumaczenie i wydanie po polsku w 1872 w Bytomiu opowiadania niemieckiego pisarza Konrada Bolandena pt .
`` Stary Bóg żyje '' został skazany przez władze niemieckie na więzienie .
Odbył 2 miesiące kary , a cały nakład książki został skonfiskowany .
Jednym z przesłań jego poezji była teza o nieustającej polskości Górnego Śląska .
Współpracował z działaczami społecznymi , wydawcami i dziennikarzami na Śląsku Józefem Szafrankiem oraz Karolem Miarką .
