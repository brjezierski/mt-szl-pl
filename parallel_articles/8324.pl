Norda
Norda – kaszubskie określenie `` Północy '' , równocześnie jeden z 5 regionów Kaszub , historycznie obejmujący współczesne powiaty pucki , wejherowski i fragmenty lęborskiego .
Kaszubi dziś zamieszkują głównie jego wschodnią i centralną część .
Obszar ten wyróżnia się również językowo na tle innych regionów Kaszub – dominuje tu północna kaszubszczyzna z ruchomym akcentowaniem i licznymi archaizmami .
W 2011 r. powstało Metropolitalne Forum Wójtów , Burmistrzów i Prezydentów `` Norda '' zrzeszające działaczy samorządowych czujących się związanymi z historycznym obszarem Nordy .
