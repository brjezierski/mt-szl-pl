Norylsk
Norylsk ( ros .
Норильск , `` Norilsk '' ) – miasto zamknięte w azjatyckiej części Rosji w Kraju Krasnojarskim , w Tajmyrskim Dołgano-Nienieckim rajonie .
Norylsk jest jednym z najbardziej wysuniętych na północ miast na świecie .
W 2020 miasto liczyło 181 830 mieszkańców i podlega bezpośrednio władzy w Kraju Krasnojarskim .
Zostało zbudowane w latach 30 .
XX wieku przez więźniów .
W czasach ZSRR rejon wielu łagrów .
Prawa miejskie uzyskało w 1953 .
Więźniowie oraz uwolnieni stanowili główną siłę roboczą do 1955 r .
Jest ośrodkiem hutnictwa i górnictwa metali nieżelaznych ( niklu , kobaltu , platyny ) oraz węgla kamiennego .
Ma jedną uczelnię , jest siedzibą Instytutu Naukowo-Badawczego Rolnictwa Dalekiej Północy .
