Narok ( hrabstwo )
Narok – hrabstwo w Kenii .
Jego stolicą jest Narok .
Według Spisu Powszechnego z 2019 roku liczy 1 157 873 mieszkańców .
Miejscowi ludzie są w większości z grupy etnicznej Masajów .
Hrabstwo Narok dzieli swoją granicę z Tanzanią na południu i hrabstwami : Nakuru na północy , Bomet , Nyamira i Kisii na północnym zachodzie , Kajiado na wschodzie i z Migori na zachodzie .
Nazwa pochodzi od Enkare Narok , rzeki przepływającej przez miasto Narok .
W hrabstwie znajduje się znany na całym świecie Rezerwat Narodowy Masai Mara , który słynie z wielkiej migracji antylop gnu .
Rolnictwo .
Główne uprawy w hrabstwie to : pszenica , jęczmień , kukurydza , fasola , trzcina cukrowa i ogrodnicze uprawy tj .
pomidory , ziemniaki , kapusta , fasolka szparagowa , cebula i inne .
Największy dochód w hrabstwie przynosi uprawa pszenicy i kukurydzy .
Hodowla zwierząt jest jedną z głównych działalności gospodarczych wspierających większość obszarów wiejskich .
Do hodowlanych zwierząt w hrabstwie należą : bydło , owce , kozy , drób , pszczoły , króliki i osły .
Populacja głównych zwierząt gospodarskich w 2009 roku wynosiła odpowiednio : 1,4 miliona sztuk bydła , 1,2 miliona owiec i 0,8 miliona kóz .
