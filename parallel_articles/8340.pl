North Shields
North Shields – miasto w północno-wschodniej Anglii ( Wielka Brytania ) , w hrabstwie metropolitalnym Tyne and Wear , w dystrykcie metropolitalnym North Tyneside , położone na północnym brzegu rzeki Tyne , około 13 km na wschód od Newcastle upon Tyne .
Wchodzi w skład aglomeracji Tyneside .
W 2001 roku miasto liczyło 39 042 mieszkańców .
W mieście rozwinął się przemysł stoczniowy oraz rybny .
