Numea
Numea ( fr .
`` Nouméa '' ) – stolica Nowej Kaledonii , terytorium zależnego Francji .
Leży na południowym wybrzeżu wyspy Nowa Kaledonia .
Zamieszkuje ją 94 285 mieszkańców ( 2019 ) .
Znajduje się tu huta niklu ( Doniambo ) , międzynarodowy port lotniczy i muzeum oraz ogród botaniczny , a także akwarium morskie .
Aglomeracja ( `` Grand Nouméa '' ) liczy 182 341 mieszkańców , co stanowi ok. 67 % ludności całego terytorium .
Edukacja .
W mieście działa Uniwersytet Nowej Kaledonii , jedyna uczelnia wyższa na wyspie , a także Centrum Kulturalne Jean-Marie Tjibaou .
