Novi Pazar
Novi Pazar ( cyr .
Нови Пазар , bośn .
`` Novi Pazar '' ) – miasto w Serbii , w regionie Serbia Centralna , w okręgu raskim , siedziba miasta Novi Pazar .
W 2013 roku liczyło 66 527 mieszkańców .
Położenie .
Novi Pazar położony jest w krainie historycznej Sandżak , w południowo-zachodniej Serbii , na terenie Gór Dynarskich , przy granicy z Czarnogórą i z nieuznawanym przez władze w Belgradzie Kosowem .
Współrzędne geograficzne to 43° 09 ” N , 20° 31 ” E. Przez miasto przepływa rzeka Raška .
Gospodarka .
W mieście rozwinął się przemysł włókienniczy oraz obuwniczy .
Historia .
Osadnictwo na terenie dzisiejszego Novego Pazaru rozwijało się już od czasów prehistorycznych .
