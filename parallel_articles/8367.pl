Historia .
Założony w końcu XVII wieku jako osiedle związane z budową twierdzy Petrovaradin , swoją nazwę obecną otrzymał 1 lutego 1748 roku , kiedy mieszkańcy kupili na dworze w Wiedniu status wolnego miasta królewskiego za kwotę 95 tysięcy forintów .
Już pod koniec XVIII wieku znajdował się w nim browar , fabryka jedwabiu i zakład tytoniowy .
Od 1826 działa w nim nieprzerwanie „ Matica Srpska ” instytucja edukacyjna z gazetą o najdłuższym stażu w Europie .
W 1849 roku Nowy Sad był po raz pierwszy bombardowany i ocalało tylko 808 budynków z 2812 .
W 1892 roku miała miejsce epidemia cholery wywołana złą jakością wody pitnej .
Do 1918 roku i w trakcie II wojny światowej , od 1941 do 1945 roku w granicach Węgier .
Podczas węgierskich rządów doszło w 1942 roku do masakry ludności serbskiej i żydowskiej w mieście i jego okolicach .
W okresie po II wojnie światowej teren miasta uległ znacznemu powiększeniu , w tym poprzez zasypanie bagnistych terenów otaczających przez wieki twierdzę .
W 1999 bombardowania NATO zniszczyły wszystkie trzy mosty i główne wodociągi przeprowadzone pod nimi , które zostały później odbudowane .
Gospodarka .
W mieście rozwinął się przemysł maszynowy , elektrotechniczny , środków transportu , włókienniczy , chemiczny , porcelanowy oraz drzewny .
Charakterystyka .
Miasto jest wielokulturowe – poza Serbami zamieszkują je Węgrzy i Rusini ; działa też wielojęzyczne radio .
Uniwersytet w Nowym Sadzie ma ponad 43 000 studentów , a wykłady są umożliwione i w językach obcych .
W Nowym Sadzie działają szkoły języka serbskiego dla cudzoziemców , zarówno uniwersyteckie , jak i samodzielne ( Azbukum ) .
