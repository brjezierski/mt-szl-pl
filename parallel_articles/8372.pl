Nové Město na Moravě ( stacja kolejowa )
Nové Město na Moravě – stacja kolejowa w miejscowości Nové Město na Moravě , w kraju Wysoczyna , w Czechach Znajduje się na wysokości 615 m n.p.m .
Stacja jest wyposażona w poczekalnię , kasy biletowe , na których można zakupić bilety na pociągi krajowe i międzynarodowe oraz zarezerwować miejsce w pociągu .
