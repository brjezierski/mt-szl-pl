Nowa Zagora
Nowa Zagora – miasto w środkowej Bułgarii , w obwodzie Sliwen , na Nizinie Górnotrackiej .
Zamieszkane przez około 24 100 mieszkańców , których liczba stale się zmniejsza .
Według oficjalnych szacunków około 12 % mieszkańców miasta stanowią mniejszości etniczne , z których dwie główne to mniejszość turecka i romska .
Miasto cechuje wysoki wskaźnik bezrobocia , który szacowany jest na 23 % .
W mieście rozwinął się przemysł maszynowy , włókienniczy oraz spożywczy .
