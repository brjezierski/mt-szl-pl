Nowe Bielice
Nowe Bielice ( do 1945 niem .
`` Neu Belz '' ) – wieś w Polsce położona na Równinie Białogardzkiej , w województwie zachodniopomorskim , w powiecie koszalińskim , w gminie Biesiekierz .
Położona jest w odległości około 7 km na północny wschód od Biesiekierza ( siedziby władz gminy ) , około 5 km od Koszalina ( siedziby władz powiatu ) oraz około 153 km od Szczecina ( siedziby władz województwa ) .
Według danych z 16 lipca 2015 r. miała 1010 mieszkańców .
Dominującą funkcją miejscowości Nowe Bielice jest funkcja mieszkaniowa oraz usługowo-produkcyjna , w mniejszym rolnicza i turystyczna .
W ostatnich latach obserwuje się wyraźną tendencje wzrostową liczby ludności , bowiem bardzo intensywnie rozwija się budownictwo jednorodzinne .
Jest to druga – za Starymi Bielicami , a przed Biesiekierzem – miejscowość co do liczby mieszkańców w gminie .
Zasoby kulturowe .
