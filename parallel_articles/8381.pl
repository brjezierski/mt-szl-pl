Nowe Delhi
Nowe Delhi ( – czyt .
: `` nai dilli '' ; – czyt .
: `` nju deli '' ) – stolica Indii znajdująca się w Delhi – jednostce administracyjnej mającej status narodowego terytorium stołecznego ( `` national capital territory '' ) .
Nowe Delhi , choć pod względem administracyjnym jest osobnym miastem , nie stanowi wydzielonego organizmu miejskiego , lecz łączy się z sąsiednimi dzielnicami Delhi , stanowiąc jakby kolejną dzielnicę tego miasta .
Potocznie nazwą Nowe Delhi określa się całe Delhi z wyłączeniem Starego Delhi .
Historia .
W starożytności starsza część miasta znana była pod nazwą Indraprastha .
W I tysiącleciu p.n.e .
miasto było stolicą państwa Pandawów .
W XII w. Delhi opanowali najeźdźcy muzułmańscy i od 1206 było stolicą odrębnego sułtanatu delhijskiego .
Od XIV w. Delhi zostało zajęte przez Timurydów .
W latach 1526–1739 Delhi było stolicą państwa Wielkich Mogołów .
W 1803 Delhi znalazło się pod panowaniem Brytyjczyków .
W 1912 Brytyjczycy przenieśli stolicę z Kalkuty do Nowego Delhi , a w 1947 stało się stolicą niepodległych Indii .
Transport .
Niedaleko od centrum miasta znajduje się Port lotniczy Indira Gandhi .
