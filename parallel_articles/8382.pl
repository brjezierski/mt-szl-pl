Drążdżewo Nowe
Drążdżewo Nowe – wieś w Polsce położona w województwie mazowieckim , w powiecie przasnyskim , w gminie Jednorożec .
Leży nad rzeką Orzyc .
Historia .
W granicach administracyjnych wsi odnaleziono ślady średniowiecznej i nowożytnej osady .
Dawniej współczesne wsie : Drążdżewo , Drążdżewo-Kujawy , Drążdżewo Małe , Drążdżewo Nowe funkcjonowały w źródłach jako jedna wieś : Drążdżewo .
W spisie powszechnym z 1921 wyróżnia się Drążdżewo , Drążdżewo-Folwark , Drążdżewo-Krzywomija , Drążdżewo-Kujawy , Drążdżewo Nowe i Drążdżewo-Stegna .
Wieś Drążdżewo Nowe powstała w wyniku parcelacji ziemi dworskiej dóbr Krasnosielc .
Po I wojnie światowej dzierżawca Zygmunt Biernacki sprzedawał ziemię .
