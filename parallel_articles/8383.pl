Grauże Nowe
Grauże Nowe – wieś w Polsce położona w województwie podlaskim , w powiecie suwalskim , w gminie Szypliszki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
W najbliższej okolicy znajdują się miejscowości : Grauże Stare , Lipowo , Klonorejść , Lipowo , Kaletnik .
Prawdopodobnie nazwa wsi powstała od litewskiego słowa gražus – piękny .
Potwierdzeniem urody wsi jest malownicze jezioro – Grauże .
Około 10 m od jeziora znajduje się dom wypoczynkowy .
