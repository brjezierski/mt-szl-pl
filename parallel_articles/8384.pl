Nowe Iganie
Nowe Iganie – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 5 km od centrum Siedlec , nad rzeką Muchawką ( dawna Wiśniówka ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Przez Nowe Iganie przebiega trasa międzynarodowa E30 Cork – Omsk , której częścią w Polsce jest droga krajowa nr 2 i droga powiatowa do Siedlec i Żelkowa Kolonii .
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Nieustającej Pomocy w Starym Opolu oraz do Parafii Błogosławionych Męczenników Podlaskich w Siedlcach .
10 kwietnia 1831 , w okresie powstania listopadowego , miała tu miejsce bitwa , w której Polacy zwyciężyli wojska rosyjskie .
We wsi znajduje się upamiętniający bitwę pomnik odsłonięty 13 września 1931 roku .
Imieniem dowódcy bitwy – gen. Ignacego Prądzyńskiego nazwano Zespół oświatowy ( szkoła podstawowa i gimnazjum ) w Nowych Iganiach .
