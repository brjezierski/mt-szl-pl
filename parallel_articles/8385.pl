Nowe Kotkowice
Nowe Kotkowice ( dodatkowa nazwa w j. niem .
Neu Kuttendorf ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Głogówek .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Kotliny Raciborskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 105 osób .
Przysiółkiem wsi jest Chudoba .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 12 km od granicy z Czechami , w zachodniej części Kotliny Raciborskiej , tuż przy granicy powiatu prudnickiego z powiatem krapkowickim ( gmina Walce ) .
Należy do Euroregionu Pradziad .
