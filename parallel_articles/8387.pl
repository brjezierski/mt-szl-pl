Nowe Miasto nad Pilicą ( stacja kolejowa )
Nowe Miasto nad Pilicą – dawna wąskotorowa stacja kolejowa w Nowym Mieście nad Pilicą , w województwie mazowieckim , w Polsce .
Stacja-kraniec linii Warszawa Mokotów - Nowe Miasto nad Pilicą .
Otwarcie linii 30 października 1898 na trasie Warszawa Mokotów - Piaseczno Miasto Wąskotorowe , z 1 torem o prześwicie : 1000 mm .
20 września 1924 otwarto ostatni zbudowany odcinek Brzostowiec – Nowe Miasto nad Pilicą z 1 torem , prześwit : 1000 mm .
Największa długość linii - przed 1937 ( po 1924 ) : 83,710 km .
Od 1937 skracanie linii , rozebranie odcinka Warszawa Mokotów - Warszawa Południowa ( skrócenie linii na ul .
Puławskiej ) .
1968-10-01 zamknięcie dla ruchu pasażerskiego odcinka Warszawa Południowa - Warszawa Dąbrówka Wąskotorowa .
1 grudnia 1969 rozebranie linii Warszawa Południowa – Warszawa Dąbrówka Wąskotorowa .
1 sierpnia 1971 koniec kursowania do Warszawy , rozebranie Warszawa Dąbrówka Wąskotorowa – Piaseczno Miasto Wąskotorowe .
Kolejne zamknięcie dla ruchu pasażerskiego 1 stycznia 1988 , odcinek Grójec – Nowe Miasto nad Pilicą .
Reszta linii - zamknięcie dla ruchu pasażerskiego w 1 lipca 1991 ( odcinek Piaseczno Miasto Wąskotorowe - Grójec ) .
Koniec ( zamknięcie ) dla ruchu towarowego w 1 września 1996 ( definitywny koniec stacji na kursach komercyjnych ) Piaseczno Miasto Wąskotorowe – Nowe Miasto nad Pilicą .
