Nowe Okniny
Nowe Okniny – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Wiśniew .
Według danych ze spisu ludności z 2002 roku wieś liczyła 150 mieszkańców .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
W odległości 600 metrów od wsi przebiega droga krajowa nr 63 Sławatycze – Węgorzewo .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Andrzeja z Awelinu w Radomyślu lub do parafii Matki Boskiej Częstochowskiej w Gręzówce .
