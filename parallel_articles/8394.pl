Nowe Opole
Nowe Opole – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży przy trasie międzynarodowej E30 Cork - Omsk , 7 km na zachód od Siedlec .
W miejscowości znajdują się Wyższe Seminarium Duchowne im .
Jana Pawła II , filia biblioteczna , remiza strażacka , sala weselna oraz plac zabaw dla dzieci .
Od 1918 roku w Nowym Opolu działa jednostka Ochotniczej Straży Pożarnej .
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Nieustającej Pomocy w Starym Opolu .
Na terenie miejscowości znajduje się także cmentarz parafialny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
