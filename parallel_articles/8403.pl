Nowa Biała ( Płock )
Nowa Biała ( dawn .
`` Biała Nowa '' ) – dawna część miasta Płocka , położona na północnych obrzeżach miasta .
Graniczy ze wsią Nowa Biała .
Nazwę zniesiono z 2023 .
Historia .
Do 1961 była to wschodnia część kolonii Nowa Biała .
Należała w latach 1867–1931 do gminy Brwilno , a w latach 1931–1954 do gminy Biała w powiecie płockim .
W Królestwie Polskim przynależała do guberni warszawskiej , a w okresie międzywojennym do woj .
warszawskiego .
Tam , 20 października 1933 utworzyła gromadę Biała Nowa w granicach gminy Biała , składającą się z samej kolonii Biała Nowa .
Podczas II wojny światowej włączona do III Rzeszy .
Po wojnie ponownie w Polsce .
W związku z reorganizacją administracji wiejskiej ( zniesienie gmin ) jesienią Biała Nowa weszła w skład nowo utworzonej gromady Biała Stara w powiecie płockim .
31 grudnia 1961 część wsi Biała Nowa wyłączono z gromady Biała Stara , włączając ją do Płocka .
