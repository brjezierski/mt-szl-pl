Nowa Kaledonia
Nowa Kaledonia ( ) – francuskie terytorium zamorskie o statusie wspólnoty szczególnego rodzaju ( `` sui generis '' ) w zachodniej części Oceanu Spokojnego , w Melanezji , około 1400 km na wschód od Australii i 1500 km na północny zachód od Nowej Zelandii .
Geografia .
W skład terytorium wchodzi główna wyspa , Nowa Kaledonia ( fr .
`` Grande Terre '' ) , wraz z przybrzeżnymi wyspami , m.in .
Wyspy Lojalności ( fr .
`` îles Loyauté '' ) , Île des Pins , Wyspy Chesterfield ( fr .
`` îles Chesterfield '' ) .
Terytorium jest często potocznie nazywane `` le Caillou '' przez mieszkańców pochodzenia europejskiego oraz `` la Kanaky '' przez mieszkańców pochodzenia melanezyjskiego .
Powierzchnia całego terytorium zamorskiego Nowej Kaledonii wynosi 18 575,5 km² ; w tym główna wyspa – 16 594,6 km² ; liczy 279 070 mieszkańców ( 2017 r. ) .
Stolicą terytorium jest Numea ( fr .
`` Nouméa '' ) w południowej części wyspy Nowa Kaledonia .
Melanezyjscy Kanakowie stanowią 42,5 % mieszkańców , Europejczycy 37,1 % , Polinezyjczycy 11,8 % .
Wnętrze głównej wyspy jest górzyste , z najwyższym szczytem Mont Panié , 1628 m n.p.m .
Klimat tropikalny , opady sezonowe , 1500 mm na Wyspach Lojalności , 2000 mm na wschodnim wybrzeżu , 4000 mm w górach , 1200 mm na zachodzie .
Nowa Kaledonia jest pochodzenia kontynentalnego – jest fragmentem Gondwany .
Najpierw , 85 mln lat temu , oddzieliła się od Australii , a 65 mln od Nowej Zelandii .
Izolacja od gatunków powstających w innych rejonach świata , takich jak ssaki , spowodowała , że wyspa stała się „ arką Noego ” dla wielu różnorodnych , prymitywnych organizmów , głównie roślin , jak np .
araukaria .
Spośród 3,3 tys .
gatunków roślin ¾ jest endemiczne – a także 5 rodzin monotypowych , w tym amborellowate .
Przed przybyciem ludzi żył tu m.in .
ptak `` Sylviornis neocaledoniae '' o wysokości 1,7 m i wadze 30 kg .
Obecnie ( 2014 r. ) żyją tu 24 endemiczne gatunki ptaków , w tym kagu będący jedynym gatunkiem w rodzinie `` Rhynochetidae '' ; dokładnie połowa z nich jest co najmniej narażona na wyginięcie .
Z 70 gatunków gadów 62 są endemiczne .
Zwierzęta te są zagrożone przez sprowadzone i zdziczałe szczury , koty , psy i świnie .
