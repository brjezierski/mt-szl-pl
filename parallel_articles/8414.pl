Nowa Sól
Nowa Sól ( niem .
`` Neusalz an der Oder '' ) – miasto w Polsce , będące jednocześnie gminą miejską i siedzibą gminy wiejskiej Nowa Sól , zlokalizowane w zachodniej Polsce , w województwie lubuskim , na Wale Trzebnickim , nad Odrą ; siedziba powiatu nowosolskiego ; wraz z Zieloną Górą i Sulechowem tworzy Lubuskie Trójmiasto .
Według danych GUS z 31 grudnia 2019 r. Nowa Sól liczyła 38 645 mieszkańców .
Jest trzecim pod tym względem miastem w woj .
lubuskim – większymi ośrodkami są Zielona Góra i Gorzów Wielkopolski .
W mieście wytyczono podstrefę Kostrzyńsko-Słubickiej Specjalnej Strefy Ekonomicznej .
Nowa Sól jest członkiem Związku Miast Polskich .
Położenie .
Nowa Sól jest położona w południowej części województwa lubuskiego , na lewym brzegu Odry na obszarze zachodniej części Wzgórz Dalkowskich ( Pradoliny Barucko-Głogowskiej ) .
Historycznie leży na skraju Dolnego Śląska .
Według danych z 1 stycznia 2010 powierzchnia miasta wynosiła 21,80 km² .
