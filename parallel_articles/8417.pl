Historia .
Miasto zostało założone w roku 1805 jako centrum administracyjne obwodu kozaków dońskich .
Budową miasta kierowali ataman kozacki Matwiej Płatow i niderlandzki architekt François Sainte de Wollant .
W pierwszych miesiącach wojny domowej w Rosji Nowoczerkask był jednym z głównych ośrodków białych , to w tym mieście oraz w Rostowie nad Donem gen. Ławr Korniłow i gen. Michaił Aleksiejew rozpoczęli tworzenie Armii Ochotniczej .
Chcąc zniszczyć tę siłę w zarodku , rząd Lenina skierował nad Don zgrupowanie pod dowództwem Władimira Antonowa-Owsiejenki , co zmusiło białych do opuszczenia miasta w końcu lutego 1918 r. Nowoczerkask znalazł się w granicach Dońskiej Republiki Radzieckiej , która przetrwała do maja tego samego roku .
