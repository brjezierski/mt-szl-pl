Nowodwór-Piaski
Nowodwór-Piaski – wieś w Polsce położona w województwie lubelskim , w powiecie lubartowskim , w gminie Lubartów .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa lubelskiego .
W lesie , do którego przylega wieś , znajduje się cmentarz żołnierzy poległych w czasie I oraz II wojny światowej .
Do cmentarza można dotrzeć nie tylko od wsi Nowodwór-Piaski , ale także od wsi Annobór , jak również drogą serwisową , która biegnie w kierunku nowodworskiego lasu od skrzyżowania ulicy Nowodworskiej z obwodnicą Lubartowa .
Wieś stanowi sołectwo gminy Lubartów .
