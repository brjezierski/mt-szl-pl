Nowosybirsk
Nowosybirsk ( , `` Nowosibirsk '' ; w latach 1904–1925 `` Nowonikołajewsk '' ) – miasto w Rosji , na Syberii , nad rzeką Ob .
Trzecie co do liczby mieszkańców ( 1 620 162 ) miasto rosyjskie po Moskwie i Petersburgu i największe miasto Syberii .
Nowosybirsk jest uważany za nieoficjalną stolicę Syberii .
