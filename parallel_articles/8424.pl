Nowotwór
Nowotwór ( łac .
`` neoplasma '' , skrót `` npl .
`` , od stgr .
`` νέος '' , nowy , i `` πλάσμα '' , twór , wytwór , kształt ) – grupa chorób , w których komórki organizmu dzielą się w sposób niekontrolowany , a nowo powstałe komórki nowotworowe nie różnicują się w typowe komórki tkanki .
Utrata kontroli nad podziałami jest związana z mutacjami genów kodujących białka uczestniczące w cyklu komórkowym : protoonkogenów i antyonkogenów .
Mutacje te powodują , że komórka wcale lub niewłaściwie reaguje na sygnały z organizmu .
Powstanie nowotworu złośliwego wymaga kilku mutacji , stąd długi ( ale najczęściej bezobjawowy ) okres rozwoju choroby .
U osób z rodzinną skłonnością do nowotworów część tych mutacji jest dziedziczona .
Dziedzinami medycyny zajmującymi się rozpoznawaniem i leczeniem chorób nowotworowych są onkologia , chirurgia onkologiczna , patomorfologia ( szczególnie histopatologia oraz cytopatologia ) .
Statystyki .
Międzynarodowa Agencja Badań nad Rakiem szacuje , że na świecie w 2008 na nowotwory zachorowało 12,7 mln osób , a 7,5 mln zmarło w wyniku choroby nowotworowej .
W Europie co roku chorobę nowotworową rozpoznaje się u 1,7 mln osób , a około 965 tysięcy ludzi umiera z jej powodu .
W Polsce rocznie notuje się w przybliżeniu 150 tysięcy zachorowań i 90 tysięcy zgonów spowodowanych rakiem .
Statystyki pokazują więc , że nowotwory stanowią poważny problem zdrowotny i społeczny .
