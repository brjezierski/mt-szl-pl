Nowy Browiniec
Nowy Browiniec ( ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Lubrza .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
Przepływa przez nią Potok Browiniecki .
W latach 1975–1998 miejscowość należała administracyjnie do województwa opolskiego .
Według danych na 31 grudnia 2013 r. wieś była zamieszkana przez 431 osób .
