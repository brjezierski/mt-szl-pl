Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 3 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy gminy Lubrza z gminą Głogówek , po obu stronach Potoku Browinieckiego .
Należy do Euroregionu Pradziad .
Leży na wysokości 230 m n.p.m .
