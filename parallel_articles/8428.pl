Nowy Dwór Prudnicki
Nowy Dwór Prudnicki ( niem .
`` Neuhof '' ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Krapkowice .
Od 1950 r. należy do województwa opolskiego .
Do 1956 roku Nowy Dwór Prudnicki należał do powiatu prudnickiego .
W związku z reformą administracyjną , w 1956 Nowy Dwór Prudnicki został odłączony od powiatu prudnickiego i przyłączony do nowo utworzonego krapkowickiego .
Nazwa tej miejscowości pochodzi od słowa „ dwór ” , co oznaczało majątek rolny lub folwark .
Wioska powstała w latach 1623–1626 , jako osada służebna wobec powstałego równocześnie folwarku należącego do hrabiego Oppersdorfa z Głogówka .
W wydanej we Wrocławiu w 1845 r. topografii Śląska odnotowano dwie nazwy tej miejscowości : „ Neuhof ” oraz Nowy Dwór .
Wtedy wioska liczyła 202 mieszkańców .
Według spisu ludności z 1 grudnia 1910 , na 200 mieszkańców Nowego Dworu Prudnickiego wszyscy posługiwali się językiem polskim .
W `` Spisie miejscowości województwa śląsko-dąbrowskiego łącznie z obszarem ziem odzyskanych Śląska Opolskiego '' wydanym w Katowicach w 1946 wieś wymieniona jest pod polską nazwą `` Nowy Dwór '' .
15 marca 1947 r. nadano miejscowości , wówczas administracyjnie należącej do powiatu prudnickiego , nazwę `` Nowy Dwór Prudnicki '' .
Mieszkańcy .
Miejscowość zamieszkiwana jest przez mniejszość niemiecką oraz Ślązaków .
Mieszkańcy wsi posługują się gwarą prudnicką , będącą odmianą dialektu śląskiego .
Należą do podgrupy gwarowej nazywanej `` Kamiyniołrzy '' .
