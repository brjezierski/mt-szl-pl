Nowy Dwór Królewski
Nowy Dwór Królewski ( wymowa : IPA : ˈnɔvɨ ˈdvur kruˈlɛfsʲci , AS3 novy dvur krulefsʹḱi ) – wieś w północnej Polsce , położona w województwie kujawsko-pomorskim , w powiecie chełmińskim ( na obrzeżu ) , w gminie Papowo Biskupie .
Po raz pierwszy wzmiankowana została w XIV w. Nowy Dwór Królewski wchodzi w skład sołectwa Niemczyk .
Wieś należy do Parafii św .
Mikołaja w Papowie Biskupim .
Miejscowość nie dzieli się na części .
W miejscowości znajduje się zabytkowy pałac .
Miejscowość należy do tzw .
`` wsi zwartych '' .
