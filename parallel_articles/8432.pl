Nowy Hanower
Nowy Hanower ( , tok pisin `` Niu Hanova '' ; także `` Lavongai '' ) – wyspa w Archipelagu Bismarcka , w południowo-zachodniej części Oceanu Spokojnego , należąca administracyjnie do Papui-Nowej Gwinei .
Znajduje się na zachód od zachodniego krańca wyspy Nowa Irlandia .
Jej południowe wybrzeża leżą nad Morzem Bismarcka , zaś północne nad otwartym Oceanem Spokojnym .
Należy do prowincji Nowa Irlandia .
Nowy Hanower ma charakter wulkaniczny .
Najwyższy szczyt wznosi się na wysokość 957 m n.p.m .
Wyspa mierzy około 60 km długości i 30 km szerokości .
Zajmuje powierzchnię 1227,1 km² , a długość jej linii brzegowej wynosi 172,2 km .
