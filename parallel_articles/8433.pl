Nowy Jiczyn
Nowy Jiczyn ( , ) – miasto powiatowe w Czechach w południowo-zachodniej części kraju morawsko-śląskiego , siedziba powiatu Nowy Jiczyn .
Liczba mieszkańców : ok. 23 tys. , a powierzchnia ok. 44 km² .
Jest częścią przemysłowej aglomeracji ostrawsko-frydeckiej .
Gospodarka to przede wszystkim przemysł lekki i rolnictwo .
Miasto położone jest nad rzeką Jičínką , prawym dopływem Odry .
Administracyjnie podzielone jest na 7 dzielnic , z tego niektóre wyraźnie o charakterze wiejskim .
Zabytkowe `` Masarykovo náměstí '' jest historycznym centrum Nowego Jiczyna .
Atrakcją turystyczną miasta jest muzeum kapeluszy mieszczące się w dawnym zamku Žerotínów .
