Nowy Lubosz
Nowy Lubosz – wieś w Polsce położona w województwie wielkopolskim , w powiecie kościańskim , w gminie Kościan .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako Lubosz nowy należała do wsi większych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Lubosz Nowy należał do okręgu kościańskiego tego powiatu i stanowił część majątku Racat ( niem .
Razoten , dziś Racot ) , który należał wówczas do byłego króla Niderlandów ( Wilhelma I ) .
Według spisu urzędowego z 1837 roku Lubosz Nowy liczył 260 mieszkańców , którzy zamieszkiwali 32 dymy ( domostwa ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa leszczyńskiego .
