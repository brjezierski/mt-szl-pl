Nowy Meksyk
Nowy Meksyk ( ang .
`` New Mexico '' , hiszp .
`` Nuevo México , '' naw .
`` Yootó Hahoodzo '' ; ) – stan na południowym zachodzie Stanów Zjednoczonych .
Od południa graniczy z Meksykiem , natomiast z pozostałych stron z innymi amerykańskimi stanami ( od zachodu Arizoną , od północy z Kolorado , od wschodu z Oklahomą i Teksasem ) .
Stolicą stanu jest Santa Fe , a największym miastem Albuquerque .
Nowy Meksyk ma najwyższy odsetek ludności latynoskiej w Stanach Zjednoczonych oraz drugi po Alasce odsetek rdzennych Amerykanów .
Geografia .
Klimat jest bardzo zróżnicowany w zależności od położenia i wysokości .
Na południu przeważają pustynie , gdzie temperatury w lecie przekraczają 38 °C , a na północy śnieżne szczyty , gdzie temperatury zimą spadają wiele stopni poniżej zera .
Chociaż Nowy Meksyk jest piątym co do wielkości stanem USA pod względem powierzchni , to jest szóstym – najmniej zaludnionym .
Około jednej czwartej ludności mieszka w mieście Albuquerque , w środkowej części stanu .
Nowy Meksyk ma wiele zalesionych dolin i szczytów w południowych Górach Skalistych , pustynnych kanionów na południu , a na wschodzie wysokich płaskowyżów Wielkich Równin .
Najwyższym szczytem jest Wheeler Peak , wznoszący się na wysokość 4013 m n.p.m .
Demografia .
Spis ludności z roku 2020 stwierdza , że stan Nowy Meksyk liczy 2 117 522 mieszkańców , co oznacza wzrost o 58 343 ( 2,8 % ) w porównaniu z poprzednim spisem z roku 2010 .
Dzieci poniżej piątego roku życia stanowią 5,8 % populacji , 22,7 % mieszkańców nie ukończyło jeszcze osiemnastego roku życia , a 18 % to osoby mające 65 i więcej lat .
50,5 % ludności stanu stanowią kobiety .
