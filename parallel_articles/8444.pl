Nowy Targ
Nowy Targ – miasto w województwie małopolskim , położone w Kotlinie Orawsko-Nowotarskiej u stóp Gorców , w widłach Czarnego i Białego Dunajca .
Siedziba powiatu nowotarskiego oraz gminy wiejskiej Nowy Targ , a także Euroregionu Tatry .
Liczący 33 293 mieszkańców Nowy Targ jest największym miastem i historyczną stolicą Podhala , jak również jego głównym ośrodkiem handlowym , komunikacyjnym i przemysłowym .
W mieście działa Podhalańska Państwowa Uczelnia Zawodowa , a także najwyżej położone lotnisko w Polsce .
Lokowany przed 1233 rokiem , Nowy Targ prawa miejskie otrzymał 22 czerwca 1346 r. od króla Kazimierza Wielkiego .
Zachował się historyczny zespół architektoniczno-urbanistyczny miasta ze średniowiecznym rynkiem .
