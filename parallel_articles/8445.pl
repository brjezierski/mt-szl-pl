Tychów Nowy
Tychów Nowy – wieś w Polsce położona w województwie świętokrzyskim , w powiecie starachowickim , w gminie Mirzec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
W miejscowości działa zespół folklorystyczny Tychowianie , występujący na różnych imprezach w regionie ( np .
: Dymarki Świętokrzyskie ) .
