Nowy Orlean
Nowy Orlean ( ang .
`` New Orleans '' , fr .
`` La Nouvelle-Orléans '' ) – miasto na południu Stanów Zjednoczonych , największe miasto stanu Luizjana .
Według spisu powszechnego przeprowadzonego w roku 2000 , liczba mieszkańców miasta wynosiła 484,7 tys. , a jego obszaru metropolitalnego – 1,32 mln .
W wyniku przejścia Huraganu Katrina liczba ludności zmalała i w 2010 roku wynosiła 343 829 osób .
Nowy Orlean położony jest w bagnistej delcie rzeki Missisipi , na jej lewym brzegu , ok. 150 km od jej ujścia do Zatoki Meksykańskiej .
Niedaleko miasta znajduje się jezioro Pontchartrain .
Przed wylewami Missisipi Nowy Orlean jest chroniony przez system tam i zapór .
Historia .
Nowy Orlean jako `` La Nouvelle-Orléans '' został założony przez francuskich osadników pod kierunkiem Jeana-Baptiste ’ a Le Moyne de Bienville ’ a .
Przybył on do Luizjany w 1699 roku .
Utworzył pierwszy fort – Fort Maurepas , a w roku 1718 wybrał miejsce pod założenie miasta , które postanowił nazwać na cześć Filipa II Burbon-Orleańskiego .
W roku 1722 stało się ono stolicą francuskiej Luizjany .
W 1762 roku przeszło pod panowanie Hiszpanii .
W 1801 roku na krótko wróciło ponownie pod władanie napoleońskiej Francji .
W 1803 roku wraz z terytorium Luizjany sprzedane przez Napoleona Bonaparte Stanom Zjednoczonym .
Na początku roku 1815 podczas ostatnich tygodni wojny brytyjsko-amerykańskiej brytyjska ekspedycja podjęła próbę zdobycia miasta .
Brytyjski szturm generalny na amerykańskie umocnienia pod miastem , przeprowadzony 8 stycznia 1815 roku i zwany bitwą pod Nowym Orleanem , został krwawo odparty przez Amerykanów .
Szybki rozwój Nowego Orleanu datuje się od początku XIX wieku , kiedy wraz ze stopniowym uregulowaniem rzeki Missisipi i rosnącym amerykańskim osadnictwem nad jej brzegami port w Nowym Orleanie stał się oknem na świat dla ogromnej połaci Ameryki Północnej .
W 1840 roku miasto liczyło już 102 tys .
mieszkańców i było największym miastem amerykańskiego Południa , jak również czwartym co do liczby ludności miastem w Stanach Zjednoczonych .
W latach 1812–1849 miasto to było stolicą stanu Luizjana , następnie przeniesioną do Baton Rouge .
W 1861 r. Luizjana ogłosiła wystąpienie z Unii i przystąpienie do Skonfederowanych Stanów Ameryki .
