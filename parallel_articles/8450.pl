Nowy Łupków ( przystanek kolejowy )
Nowy Łupków – przystanek kolejowy w Nowym Łupkowie , w województwie podkarpackim , w Polsce , na linii Zagórz – Łupków .
Znajduje się tu jeden peron .
11 listopada 2015 na teren stacji przetransportowano trzy wagony ( salonka i dwa wagony sypialne ) z 1974 z dawnego składu rządowego Edwarda Gierka , zakupione w 2015 roku przez Fundację Promocji i Rozwoju Bieszczad w celu stworzenia Bieszczadzkiego Muzeum Kolejnictwa , którego ostatecznie nie powołano .
Zachowany w oryginalnym stanie pociąg używany był do początku XXI wieku .
Korzystali z nich m.in .
Edward Gierek , Lech Wałęsa i Aleksander Kwaśniewski .
Ze stacją skomunikowana jest końcowa stacja Bieszczadzkiej Kolejki Leśnej , prowadzącej przez Smolnik , Majdan , Cisną do Wetliny ( od lat 90 .
XX wieku stacja nieczynna ) .
W roku 2017 przystanek obsługiwał 0–9 pasażerów na dobę .
