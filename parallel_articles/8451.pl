Nowy Śląsk
Nowy Śląsk ( ) – prowincja pruska istniejąca w latach 1795–1807 .
Była to odrębna jednostka administracyjna , która nie była położona na terenie historycznego Śląska .
Została ona utworzona ze skrawka byłego województwa krakowskiego , od 1793 ziemi żarnowieckiej .
Są to okręgi : Siewierza , Pilicy i Lelowa ze stolicą w Żarkach , anektowane przez Prusy w III rozbiorze Polski .
W 1807 r. obszar ten włączono do departamentu kaliskiego Księstwa Warszawskiego .
W 3 lata później przeniesiono go jednakże do departamentu krakowskiego Księstwa .
Obecnie ziemie te , stanowiące obszar Zagłębia Dąbrowskiego , znajdują się w granicach województwa śląskiego .
