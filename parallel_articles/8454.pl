Gmina weszła w skład powiatu lwowskiego ( `` Kreishauptmannschaft Lemberg '' ) , należącego do dystryktu Galicja w Generalnym Gubernatorstwie .
W skład gminy wchodziły miejscowości : Barszczowice , Ceperów , Jaryczów Nowy , Jaryczów Stary , Kukizów , Pikułowice , Podliski Wielkie , Remenów i Rudańce .
Po wojnie obszar gminy włączono do ZSRR .
