Jaryczów Nowy ( gmina )
Gmina Jaryczów Nowy – dawna gmina wiejska funkcjonująca w latach 1941–1944 pod okupacją niemiecką w Polsce .
Siedzibą gminy był Jaryczów Nowy .
Gmina Jaryczów Nowy została utworzona przez władze hitlerowskie z terenów okupowanych przez ZSRR w latach 1939–1941 , stanowiących przed wojną części gmin Jaryczów Stary i Prusy oraz miasto Jaryczów Nowy , które pozbawiono praw miejskich ( wszystkie te gminy zostały zniesione pod okupacją ) .
Tereny te należały przed wojną do powiatu lwowskiego w woj .
