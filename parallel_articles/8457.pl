Nowa Wieś Prudnicka
Nowa Wieś Prudnicka ( dodatkowa nazwa w j. niem .
Neudorf , ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Biała .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Kotliny Raciborskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 209 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 10 km od granicy z Czechami , w zachodniej części Kotliny Raciborskiej , tuż przy granicy gminy Biała z gminą Strzeleczki .
Należy do Euroregionu Pradziad .
Od północy sąsiaduje z rezerwatem wodno-leśnym Urszulanowice .
Ma charakter rolniczy .
Środowisko naturalne .
W Nowej Wsi Prudnickiej panuje klimat umiarkowany ciepły .
