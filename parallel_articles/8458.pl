Wieszczyna
Wieszczyna ( potocznie : `` Nowa Wieś '' , ) – przysiółek wsi Dębowiec w Polsce położony w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Miejscowość wchodzi w skład sołectwa Dębowiec .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Gór Opawskich .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Według danych na 2016 przysiółek był zamieszkany przez 23 osoby .
Geografia .
Przysiółek położony jest w południowo-zachodniej Polsce , w województwie opolskim , około 1,75 km od Dębowca i 650 m od granicy z Czechami , w północno-wschodniej części Gór Opawskich , na ziemi prudnickiej .
Miejscowość leży na terenie Parku Krajobrazowego Gór Opawskich , u podnóża Długoty , najwyższego szczytu powiatu prudnickiego .
Wieś położona jest w zachodniej części Lasu Prudnickiego .
Przez wieś przepływa bezimienny dopływ Zameckiego Potoku spod góry Svatý Roch .
Leży na wysokości 330 m n.p.m .
Przebiega tędy droga powiatowa z Prudnika do Pokrzywnej .
