W skład jednostki weszły obszary dotychczasowych gromad Nozdrzec i Wara ze zniesionej gminy Nozdrzec w tymże powiecie .
Dla gromady ustalono 23 członków gromadzkiej rady narodowej .
1 stycznia 1960 do gromady Nozdrzec włączono obszar zniesionej gromady Hłudno w tymże powiecie .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 w powiecie brzozowskim reaktywowano gminę Nozdrzec .
