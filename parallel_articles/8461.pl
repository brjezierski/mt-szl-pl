Nosibądy
Nosibądy ( dawniej niem .
`` Naseband '' ) – wieś w północno-zachodniej Polsce położona w województwie zachodniopomorskim , w powiecie szczecineckim , w gminie Grzmiąca .
Do 1687 r. wieś należała do rodziny Glasenapp .
Następnie właścicielem był Joachim von Kleist i jego następcy .
W roku 1735 należał do rodu von Bonin .
Ostatnim właścicielem przed 1945 r. był Hans von Zastrow .
We wsi ruiny pałacu z połowy XIX w. w otoczeniu dawnego parku .
Kościół ryglowy z pierwszej połowy XVI w. rozbudowany w 1775 .
