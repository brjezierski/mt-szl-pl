Makaron
Makaron – produkt żywnościowy wytwarzany na bazie mąki , wody i niekiedy jaj ( mówimy wtedy o makaronie jajecznym ) oraz innych składników , o szerokiej gamie kształtów i zastosowań .
Historia .
Najstarszy makaron znaleziono w Chinach , miał cztery tysiące lat i ok. 20 cali długości .
Znaleziono go w wiosce z okresu neolitu nad brzegami Żółtej Rzeki .
To najstarsze odkryte przez archeologów jedzenie .
Według legendy do Europy miał go jakoby sprowadzić wenecki podróżnik , Marco Polo .
Jest to jednak tylko legenda , bowiem już tysiąc lat wcześniej , w II wieku naszej ery , rzymski badacz Galen wspomniał o tego typu potrawie , natomiast Muhammad al-Idrisi udokumentował istnienie suszonego makaronu ( takiego , jak współczesny ) w południowych Włoszech , na Sycylii i w Trabii ok. XII wieku , czyli jeszcze przed narodzinami Marco Polo .
