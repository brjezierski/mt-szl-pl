Geografia Nunavut .
Ukształtowanie powierzchni .
Nunavut obejmuje nizinny , tundrowy obszar kontynentalny wzdłuż północno-zachodnich wybrzeży Zatoki Hudsona oraz wybrzeża Morza Arktycznego do południka 122°W .
Obszar terytorium zajmuje także szereg wysp arktycznych ( do największych należą : Wyspa Wiktorii , Ziemia Baffina , Wyspa Devon , Wyspa Księcia Walii , Wyspa Melville ’ a ) .
Większość z nich ma charakter nizinnej pustyni arktycznej .
Jedynie na wielkiej Wyspie Bafina rozciąga się wysoki grzbiet górski .
Wody śródlądowe .
Kontynentalna część terytorium , leżąca w dziale wodnym Zatoki Hudsona , poprzecinana jest licznymi strumieniami , tworzącymi szerokie rozlewiska w postaci wielu jezior .
Wyspy arktyczne mają swoje indywidualne cechy .
Klimat .
Nunavut rozciągnięty od równoleżnika 60°N do bieguna leży w strefie arktycznej i subarktycznej .
W kontynentalnej części panuje klimat subarktyczny z krótkim , zaledwie dwumiesięcznym latem .
Średnia temperatura lipca wynosi około 11 °C , lecz w słoneczne dni powietrze może być nagrzane nawet do temperatury 25 °C .
W zimie temperatury spadają poniżej –30 °C .
Bogactwa naturalne .
Przez długi czas nie interesowano się bogactwami naturalnymi tego obszaru .
Dopiero perspektywa utworzenia nowego terytorium nasiliła poszukiwania geologiczne .
Zainicjowane i ciągle kontynuowane badania geologiczne wskazują na znaczne zasoby – złota , diamentów oraz innych kamieni szlachetnych , rud żelaza , ropy naftowej i gazu ziemnego .
Gospodarka .
Tradycyjna gospodarka terenów oparta jest na łowiectwie , rybołówstwie i rękodziele .
Finansowo terytorium uzależnione jest od znacznych dotacji federalnych .
Sytuacja ulega jednak powolnej zmianie .
Systematyczne badania geologiczne ukazują olbrzymi potencjał drzemiący w terytorium .
Plany na przyszłość uwzględniają rozpoczęcie wydobycia wielu cennych minerałów , których pokłady wciąż są odkrywane .
Statystyki ostatnich lat pokazują , że gospodarka Nunavut rozwija się pięciokrotnie szybciej niż całej Kanady .
