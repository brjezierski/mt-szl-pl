Nunavut
Nunavut ( ang .
wym .
; inuktitut ᓄᓇᕗᑦ , wym . )
– terytorium Kanady .
Nunavut w inuktitut oznacza „ nasz kraj ” .
Nunavut graniczy od zachodu z Terytoriami Północno-Zachodnimi od południa z Manitobą .
Posiada też południową granicę morską z Quebekiem i Ontario ( przez Zatokę Hudsona ) i na wschodzie poprzez Cieśninę Davisa , Naresa i Morze Baffina z Grenlandią .
Terytorium zawiera wody i lody dochodzące do bieguna północnego .
Terytorium zostało utworzone w 1999 z terenów Terytoriów Północno-Zachodnich .
Ustawa o Nunavut , została uchwalona przez Parlament Kanady .
Ustawa została przygotowana na podstawie roszczenia Nunavut z 1993 .
Około 85 % ludności terytorium stanowią Inuici .
Językiem urzędowym terytorium obok angielskiego i francuskiego są języki inuktitut i inuinnaqtun .
Struktura polityczna i administracyjna terytorium .
W Nunavut nie działają partie polityczne .
Całe terytorium podzielone jest na 19 jednomandatowych okręgów wyborczych , w których wybierani są przedstawiciele do terytorialnego Zgromadzenia Legislacyjnego – `` teritorial Legislative Assembly '' .
Spośród tych 19 osób w wyniku wewnętrznych decyzji wybierane są osoby premiera ( do 1994 r. funkcja ta była nazywana `` Government Leader '' – przewodniczącego rządu ) i marszałka zgromadzenia .
