Historia Nunavut .
Nunavut , terytorium Kanady powstałe w 1999 , w większości zamieszkane jest przez naród Inuitów .
Począwszy od X w. rozwinął on bardzo specyficzną kulturę opartą na łowiectwie .
Od XVII w. Inuici rozpoczęli intensywne kontakty z Kompanią Zatoki Hudsona , które doprowadzając do znacznego postępu materialnego narodu , zniszczyły jego tradycyjną kulturę .
Wycofanie się Kompanii z handlu w rejonach arktycznych w latach trzydziestych XX w. doprowadziło Inuitów na krawędź zagłady biologicznej .
Począwszy od lat 70 .
Inuici przechodzą odrodzenie narodowe , kulturowe i gospodarcze , które doprowadziło do powstania samorządnego terytorium .
