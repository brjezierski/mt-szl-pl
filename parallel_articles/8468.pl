Nurmes
Nurmes – miasto w Finlandii w regionie Karelia Północna , położone nad jeziorem Pielinen .
W latach 1997-2009 miasto należało administracyjnie do prowincji Finlandia Wschodnia .
W mieście działa parafia prawosławna Świętych Piotra i Pawła , wchodząca w skład Fińskiego Kościoła Prawosławnego .
