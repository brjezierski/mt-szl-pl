Nymburk
Nymburk ( niem .
`` Nimburg '' , `` Neuenburg an der Elbe '' ) – miasto powiatowe w Czechach w kraju środkowoczeskim nad rzeką Łabą , 53 km na wschód od Pragi .
Jako miasto istnieje od XIII w. ( prawa miejskie nadał Przemysław Ottokar w 1275 ) na terenach , gdzie osadnictwo sięga czasów prehistorycznych .
Z okresu średniowiecznego zachował się gotycki kościół i – w dużej mierze zrekonstruowane i przebudowane – potężne mury obronne .
Miasto zniszczone podczas wojny trzydziestoletniej .
Oprócz wymienionych , warte obejrzenia są jeszcze :
Komunikacja .
W mieście znajdują się dwie stacje kolejowe Nymburk hlavní nádraží i Nymburk město .
Gospodarka .
W mieście rozwinął się przemysł maszynowy , odzieżowy oraz spożywczy .
