Nędza ( stacja kolejowa )
Nędza – stacja kolejowa położona we wsi Nędza , w powiecie raciborskim , w województwie śląskim .
W 2017 stacja obsługiwała 100–149 pasażerów na dobę
Historia .
Stacja Nędza wielokrotnie zmieniała swoją nazwę .
Od 1846 do 1896 nosiła nazwę `` Nensa '' , od 1896 do 1914 – `` Nendza '' , od 1914 do 1945 – `` Buchenau '' i ostatecznie od 1945 – `` Nędza '' .
Kiedyś istniała tutaj wieża wodna .
Informacje .
Nędza jest ważną stacją przesiadkową , ponieważ łączy linię kolejową z Rybnika w kierunku Raciborza z linią z Kędzierzyna Koźla .
Między peronami z których odjeżdżają pociągi w kierunku Rybnika a peronami przy których znajdują się tory do Kędzierzyna Koźla wznosi się nieczynny budynek dworca kolejowego .
W końcowej części peronu ( w kierunku Raciborza ) znajduje się jednoosobowy schron z okresu II wojny światowej .
Przy stacji znajduje się także kilka bocznych torów postojowych na których na wolne tory lub jazdę manewrową oczekują składy towarowe .
