Powiat nyski
Powiat nyski – powiat w Polsce ( województwo opolskie ) , utworzony w 1999 roku w ramach reformy administracyjnej .
Jego siedzibą jest miasto Nysa .
W skład powiatu wchodzą :
Według danych z 31 grudnia 2019 roku powiat zamieszkiwało 135 948 osób .
Natomiast według danych z 30 czerwca 2020 roku powiat zamieszkiwały 135 524 osoby .
Geografia .
Historycznie większość terenu powiatu leży na Dolnym Śląsku , lecz często jest kojarzony z Górnym Śląskiem – w 1815 roku został włączony do rejencji opolskiej .
Powiat położony jest w południowo-zachodniej części województwa opolskiego , we wschodniej części Przedgórza Sudeckiego na Obniżeniu Otmuchowskim .
Granice powiatu wyznaczają Góry Opawskie – najdalej na wschód wysunięte pasmo Sudetów .
Od południa powiat graniczy z Czechami na odcinku ok. 70 km .
Komunikacja .
Odległości do większych ośrodków miejskich ze stolicy powiatu wynoszą około : Opole – 50 km , Wrocław – 80 km , Katowice – 160 km .
Główne drogi przebiegające przez powiat tworzą układ gwiazdy z centrum w Nysie .
