Nzérékoré
Nzérékoré – miasto w południowo-wschodniej Gwinei , stolica regionu Nzérékoré .
Jest handlowym ośrodkiem regionu o tej samej nazwie , który ma charakter rolniczy ( uprawa ryżu , palmy oliwnej , kawy , tytoniu , hodowla ) .
W pobliżu Nzérékoré znajdują się bogate złoża rudy żelaza .
Jest ważnym węzłem dróg kołowych .
Miasto posiada też lotnisko .
Miasto zamieszkuje 280 256 mieszkańców w 2012 roku .
Jest drugim co do wielkości miastem Gwinei .
