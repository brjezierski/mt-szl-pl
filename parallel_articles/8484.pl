( 5725 ) Nördlingen
( 5725 ) Nördlingen – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 144 dni w średniej odległości 2,68 j.a .
Została odkryta 23 stycznia 1988 roku w Obserwatorium Palomar przez Carolyn Shoemaker .
Nazwa planetoidy pochodzi od niemieckiego miasta Nördlingen , położonego w centrum krateru uderzeniowego Ries .
Przed jej nadaniem planetoida nosiła oznaczenie tymczasowe ( 5725 ) 1988 BK2 .
