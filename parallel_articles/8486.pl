Nørresundby
Nørresundby – miasto w Danii , na Nørrejyske Ø , u szczytu Półwyspu Jutlandzkiego .
Miasto administracyjnie należy do gminy Aalborg i regionu Jutlandia Północna ( d. okręgu Nordjyllands Amt ) .
Nørresundby położone jest nad cieśniną Limfjorden ; po drugiej stronie cieśniny leży Aalborg .
Miasta połączone są ze sobą dwoma mostami ( jeden dla ruchu kolejowego , drugi dla samochodów ( zob .
trasa europejska E39 ) oraz pieszych ) , dzięki czemu praktycznie tworzą jeden organizm miejski , m.in .
ze wspólnymi liniami autobusowymi .
