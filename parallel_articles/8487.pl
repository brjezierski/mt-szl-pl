Norymberga ( Teatr Telewizji )
Norymberga – spektakl telewizyjny `` Sceny Faktu '' Teatru Telewizji z 2006 , którego autorem jest Wojciech Tomczyk , reżyserem Waldemar Krzystek .
Dramat dotyka problematyki rozliczenia zbrodni okresu komunistycznego w Polsce .
Fabuła .
Dziennikarka odwiedza w domu byłego pułkownika kontrwywiadu PRL Stefana Kołodzieja .
Wojskowy , sięgając do czasów powojennych opowiada historię swojego życia , rozpoczynając od momentu , w którym został zwerbowany przez oficera peerelowskiej bezpieki .
