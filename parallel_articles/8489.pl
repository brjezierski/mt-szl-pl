Liczba
Liczba – pojęcie abstrakcyjne , jedno z najczęściej używanych w matematyce .
Pierwotnie liczby służyły do porównywania wielkości zbiorów przedmiotów ( liczby naturalne ) , później także wielkości ciągłych ( miary i wagi ) , obecnie w matematyce są rozważane jako twory abstrakcyjne , w oderwaniu od ewentualnych fizycznych zastosowań .
W matematyce określenie „ liczba ” bez żadnego przymiotnika jest nieścisłe , gdyż matematycy nie definiują „ liczb ” , lecz „ liczby naturalne ” , „ liczby całkowite ” itp .
Poszczególne rodzaje liczb są definiowane za pomocą aksjomatów lub konstruowane z bardziej podstawowych pojęć , jak zbiór , czy typy liczb prostsze od konstruowanego .
Zastosowania .
Najprostsze rodzaje liczb , jak liczby naturalne czy rzeczywiste , są w powszechnym użyciu jako oznaczenia ilości przedmiotów ( np .
pięć jabłek ) lub mnożnika pewnej jednostki miary ( np .
dwa i pół metra ) .
Zapisy liczb naturalnych są używane także jako identyfikatory , np .
numery telefonów , dróg , PESEL , ISBN .
W matematyce pojęcie liczby zostało rozszerzone z poznawanych w szkole podstawowej liczb naturalnych , wymiernych i rzeczywistych na takie abstrakcje , jak liczby zespolone , p-adyczne , kwaterniony , czy sedeniony .
Liczby zespolone okazały się przydatne w wielu dziedzinach od grafiki komputerowej , przez elektronikę , teorię płynów , aż do fizyki kwantowej i teorii względności .
Kwaterniony znalazły zastosowanie w grafice trójwymiarowej do prostego obliczania obrotów w przestrzeni ( zob .
współrzędne jednorodne ) .
Liczby p-adyczne znalazły zastosowanie w kryptografii .
Opis intuicyjny .
Poniższe opisy w żadnym wypadku nie są ścisłymi definicjami .
Liczby są jednak w matematyce definiowane ściśle , i definicje te są przedstawione w wydzielonym artykule .
Poniżej podane są opisy tylko kilku najprostszych zbiorów liczbowych .
Liczby naturalne .
Najczęściej używanymi liczbami są liczby naturalne .
Wśród matematyków istnieją dwie szkoły :
Z punktu widzenia aksjomatyki kwestia zaliczenia zera do liczb naturalnych jest czysto umowna i nie sprawia żadnych problemów pod warunkiem konsekwentnego trzymania się tej umowy podczas rozumowania .
Liczby całkowite .
Liczby ujemne to liczby mniejsze od zera .
Dla każdej dodatniej liczby ( czyli większej od zera ) można wskazać liczbę do niej przeciwną , czyli liczbę ujemną leżącą na osi liczbowej w tej samej odległości od zera .
