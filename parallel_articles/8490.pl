N ’ Golo Kanté
N ’ Golo Kanté ( ur .
29 marca 1991 w Paryżu ) – francuski piłkarz malijskiego pochodzenia występujący na pozycji pomocnika w angielskim klubie Chelsea oraz w reprezentacji Francji .
Wychowanek US Boulogne , w swojej karierze grał także w takich zespołach jak SM Caen oraz Leicester City .
Od 2016 roku jest reprezentantem Francji .
Wystąpił na Mistrzostwach Europy 2016 , gdzie wraz z reprezentacją zajął drugie miejsce .
Na Mistrzostwach Świata 2018 , gdzie wraz z reprezentacją zajął pierwsze miejsce i zdobył mistrzostwo świata .
Życie prywatne .
Rodzice Kanté wyemigrowali do Francji z Mali w 1980 roku .
Dorastał w małej miejscowości w Rueil-Malmaison w podparyskim departamencie Hauts-de-Seine .
Paul Fletcher – jeden z dziennikarzy BBC Sport , powiedział , że dla Kanté `` „ pieniądze nie są najważniejsze i nie interesuje się tym ile zarabia ” '' .
Kariera klubowa .
