Oberhausen ( powiat Neuburg-Schrobenhausen )
Oberhausen – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Ingolstadt , w powiecie Neuburg-Schrobenhausen .
Leży około 10 km na północny wschód od miasta Neuburg an der Donau , nad rzeką Dunaj , przy drodze B16 i linii kolejowej Ingolstadt – Donauwörth .
Dzielnice .
W skład gminy wchodzą następujące dzielnice : Oberhausen , Sinning , Unterhausen , Unterhauser Forst i Kreut .
Polityka .
Wójtem gminy jest Fridolin Gößl z CSU , rada gminy składa się z 14 osób .
