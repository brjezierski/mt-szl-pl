Oberstaufen
Oberstaufen – uzdrowiskowa gmina targowa w południowych Niemczech , w kraju związkowym Bawaria , w rejencji Szwabia , w regionie Allgäu , w powiecie Oberallgäu .
Leży w Allgäu , około 20 km na zachód od Sonthofen , przy drodze B308 i linii kolejowej Lindau ( Bodensee ) -Augsburg i granicy z Austrią .
Oberstaufen jest jednym w ważniejszych ośrodków sportów zimowych w regionie .
