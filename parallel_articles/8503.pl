Obersöchering
Obersöchering – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Oberland , w powiecie Weilheim-Schongau , wchodzi w skład wspólnoty administracyjnej Habach .
Leży około 12 km na południowy wschód od Weilheim in Oberbayern , przy drodze B472 .
Polityka .
Wójtem gminy jest Josef Kennerknecht , rada gminy składa się z 12 osób .
Oświata .
W gminie znajduje się przedszkole oraz szkoła podstawowa .
