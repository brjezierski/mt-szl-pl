Ochtrup
Ochtrup – miasto w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Münster , w powiecie Steinfurt , przy granicy z Dolną Saksonią i Holandią .
W 2010 zamieszkiwało je 19 430 osób .
Historia .
Teren , na którym położone jest współczesne miasto zasiedlony był już w epoce neolitu .
Miejscowość po raz pierwszy wzmiankowane w 1143 jako `` Ochtepe '' .
Jako wieś parafialna wzmiankowana po raz pierwszy w 1203 .
W latach 1595 i 1598 spalona przez Hiszpanów podczas wojny hiszpańsko-niderlandzkiej .
