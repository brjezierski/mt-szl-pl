Octave Mirbeau urodził się w rodzinie średnio zamożnych mieszczan .
Na naukę wysłano go do kolegium jezuickiego w Vannes , skąd został usunięty w niejasnych okolicznościach , na które światło rzuca jego powieść `` Sébastien Roch '' ( tł .
pol .
