II rozdział powieści `` Le Calvaire '' , który wywołał skandal wśród francuskich nacjonalistów ) .
Porzuciwszy studia prawnicze , rozpoczął pracę jako dziennikarz , jednak dopiero po dwunastu latach udało mu się uzyskać niezależność wystarczającą do głoszenia własnych podglądów .
Wtedy zaczęły się ukazywać jego pierwsze powieści ; w tym samym okresie rozpoczął również w prasie walkę o uznanie nowych prądów w literaturze i w sztuce .
