Odincowo
Odincowo ( ros .
`` Одинцово '' ) – miasto w Rosji , w obwodzie moskiewskim położone 24 km na zachód od Moskwy , centrum administracyjne rejonu odincowskiego .
Wieś założona w 1627 roku , nazwę wzięła od nazwiska założyciela , bojara Dymitra Dońskiego Andrieja Iwanowa Odinca ( Андрея Ивановича Одинца ) .
Rozwój w związku ze znalezieniem się na trasie linii kolejowej Moskwa – Smoleńsk i od 1957 miasto .
Klub siatkarski `` Iskra Odincowo '' .
Od 2021 r. miasto jest siedzibą eparchii odincowskiej .
W mieście rozwinął się przemysł materiałów budowlanych , chemiczny oraz metalowy .
