Odrynki
Odrynki ( białorus .
Адрынкі ) – wieś w Polsce położona w województwie podlaskim , w powiecie hajnowskim , w gminie Narew .
W latach 1975–1998 miejscowość administracyjnie należała do województwa białostockiego .
Prawosławni mieszkańcy wsi należą do parafii Podwyższenia Krzyża Pańskiego w Narwi , a wierni kościoła rzymskokatolickiego do parafii Wniebowzięcia Najświętszej Maryi Panny i św .
Stanisława Biskupa i Męczennika w Narwi .
