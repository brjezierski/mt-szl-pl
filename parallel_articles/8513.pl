Oederan
Oederan – miasto w Niemczech , w kraju związkowym Saksonia , w okręgu administracyjnym Chemnitz , w powiecie Mittelsachsen ( do 31 lipca 2008 w powiecie Freiberg ) .
Do 31 grudnia 2011 siedziba wspólnoty administracyjnej Oederan , która dzień później została rozwiązana .
Przez miasto przebiega droga krajowa B173 .
Geografia .
Oederan leży ok. 14 km na zachód od Freiberga .
