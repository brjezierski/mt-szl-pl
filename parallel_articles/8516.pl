Oer-Erkenschwick
Oer-Erkenschwick [ ] − miasto w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Münster , w powiecie Recklinghausen .
W 2010 roku liczyło 30 312 mieszkańców .
Stadthalle Oer-Erkenschwick – centrum kulturalne i konferencyjne znajdujące się w centrum miasta przy placu Berlińskim jest miejscem wydarzeń kulturalnych , koncertów , imprez artystycznych .
Dysponuje nowoczesną salą dla 700 osób o bardzo dobrej akustyce , restauracją i miejscami hotelowymi , oraz zapewnia profesjonalny sprzęt konferencyjny i audiowizualny .
