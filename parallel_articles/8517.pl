Oerlinghausen
Oerlinghausen ( dolnoniem .
`` Ankhiusen '' ) – niemieckie miasto w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Detmold , w powiecie Lippe .
Leży w Lesie Teutoburskim , w sąsiedztwie Bielefeldu w Północnej-Westfalii , na średniej wysokości 214 m n.p.m .
W Oerlinghausen znajduje się największe w Europie lotnisko szybowcowe .
