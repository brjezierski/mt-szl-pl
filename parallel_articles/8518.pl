Offa ( miasto )
Offa – miasto w południowo-wschodniej Nigerii ( Kwara ) .
Liczba mieszkańców w 2008 roku wynosiła ok. 145 tys .
W mieście rozwinięty jest przemysł włókienniczy i tkacki .
Do barwienia tkanin miejscowi tkacze wykorzystują rośliny indygowca .
Miasto posiada połączenie kolejowe m.in .
z Lagos .
