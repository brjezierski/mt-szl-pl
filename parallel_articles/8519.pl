Offenbach am Main
Offenbach am Main – miasto na prawach powiatu w Niemczech , w kraju związkowym Hesja , w rejencji Darmstadt .
Miasto liczy mieszkańców ( dane z 30 września 2015 ) i jest 5. pod względem wielkości miastem w regionie Ren-Men .
Jest ważnym ośrodkiem przemysłowym , usługowym i handlowym .
Najbliższe duże miasto – Frankfurt nad Menem – znajduje się bezpośrednio przy zachodniej i północno-zachodniej granicy miasta .
Offenbach am Main należy do dziewięciu miast Hesji o największym znaczeniu regionalnym ( niem .
`` Oberzentrum '' ) .
Geografia .
Offenbach am Main leży na południowo-wschodnim brzegu rzeki Men , naprzeciwko frankfurckich dzielnic Ostend i Fechenheim oraz na wschód od dzielnic Oberrad i Sachsenhausen-Süd .
