Język ojracki
Język ojracki – język należący do grupy zachodniej języków mongolskich ( ałtajska grupa językowa ) .
Używany przez Ojratów w zachodniej części Mongolii oraz w chińskim regionie autonomicznym Sinciang i prowincji Qinghai przez około 285 tys .
