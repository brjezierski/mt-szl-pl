Eurazja ( czasopismo )
Eurazja – czasopismo wydawane przez warszawski Ośrodek Studiów Wschodnich w latach 1994–1997 .
Poświęcone było problematyce regionu Europy Wschodniej , Kaukazu i Azji Centralnej .
Zawierało pogłębione analizy dotyczące sytuacji politycznej , gospodarczej i społecznej państw poradzieckich oraz wywiady ze znanymi politykami , intelektualistami i ekspertami .
Pismo zaplanowane było jako miesięcznik , jednak ukazywało się nieregularnie .
Łącznie wydanych zostało dziewięć numerów .
`` Eurazja '' uznawana była za jeden z najważniejszych projektów analityczno-edytorskich w historii OSW .
Dostarczana była do administracji państwowej , ale zarazem miała `` wszelkie cechy wyrafinowanego edycyjnie periodyku analitycznego na bardzo wysokim poziomie merytorycznym .
`` Eurazja '' stała się centrum refleksji analitycznej środowiska zgrupowanego wokół Ośrodka '' .
Autorzy .
Na łamach pisma publikowali autorzy zarówno z Polski , państw byłego ZSRR oraz Zachodu .
Wśród nich byli m.in .
: Jacek Cichocki , Wojciech Jagielski , Dawid R. Marples , Tadeusz A. Olszański , Katarzyna Pełczyńska-Nałęcz , Wojciech Zajączkowski , Jan Rowiński , Tadeusz Świętochowski , Siergiej Karaganow , Wołodymyr Kułyk , Witalij Portnikow , Wsiewołod Czaplin , Arif Junosow , James Sherr , Dmitrij Trenin , Andrzej Grajewski , Marina Pawłowa-Silwanska , Roman Szporluk , Walerij Karbalewicz , Janusz Cisek , Iwan Łysiak-Rudnycki , Jakub M. Godzimirski .
`` Eurazja '' publikowała również klasyczne teksty dotyczące polskiej polityki wschodniej .
Jako pierwszy periodyk po 1989 przypomniała postać Włodzimierza Bączkowskiego , w tym jego słynny artykuł z 1935 `` Nie jesteśmy ukrainofilami '' .
Tekst wprowadzający napisał Jerzy Giedroyc .
