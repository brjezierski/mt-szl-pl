Euro
Euro ( znak : € , kod ISO 4217 : EUR ; bułg .
`` евро '' , trb .
`` ewro '' ; gr .
`` ευρώ '' ; łot .
`` eiro '' ; lit .
`` euras '' ; malt .
`` ewro '' ; słoweń .
`` evro '' ; węg .
`` euró '' ) – nazwa waluty wprowadzonej w większości państw Unii Europejskiej , a także innych , w miejsce walut krajowych .
W formie gotówkowej walutę euro wprowadzono do obiegu dnia 1 stycznia 2002 roku .
Euro jest prawnym środkiem płatniczym w 20 państwach tworzących w Unii Europejskiej strefę euro a obejmującą swym obszarem około 341 mln Europejczyków .
Waluta euro używana jest także w 11 krajach i terytoriach nienależących do UE .
W Watykanie , Monako , San Marino i Andorze przed wprowadzeniem euro do transakcji gotówkowych obowiązywały waluty krajów unijnych , z którymi sąsiadują .
Pragmatyzm nakazywał , by i u nich obowiązywało euro .
W częściowo uznawanym państwie Kosowo i w Czarnogórze , które uzyskały niepodległość dużo później niż wejście w życie euro i zdecydowały się nie tworzyć własnych walut , lecz wybrać jedną z najbardziej znaczących na świecie .
Euro obowiązuje też w odległych od Europy francuskich posiadłościach na Oceanie Atlantyckim i Indyjskim oraz w brytyjskich bazach wojskowych na Cyprze .
Strefa euro ( eurostrefa , euroland , obszar euro ) jest tworzącą unię walutową wspólną przestrzenią unijnych państw , które wprowadziły euro jako swoją walutę , gdzie Europejski Bank Centralny prowadzi niezależną politykę pieniężną .
Przy kwocie w obiegu wynoszącej ponad 751 miliardów euro jest walutą o najwyższej wartości gotówkowej w świecie .
Na euro przypada 27 % światowych rezerw walutowych .
