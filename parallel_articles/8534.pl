Jesiotr zachodni
Jesiotr zachodni , jesiotr właściwy , jesiotr ( `` Acipenser sturio '' ) – gatunek wędrownej , anadromicznej ryby z rodziny jesiotrowatych ( Acipenseridae ) .
Występowanie .
Wzdłuż europejskich brzegów Oceanu Atlantyckiego , Morze Śródziemne i Morze Czarne .
W jeziorach Ładoga i Onega tworzy formę wyłącznie słodkowodną .
Jego obecność w Morzu Bałtyckim , została zakwestionowana w wyniku badań molekularnych , przeprowadzonych na osobniku złowionym w 1996 r. w Estonii .
Badania te wskazują , że populacja jesiotrów bałtyckich wywodzi się od jesiotra ostronosego ( `` Acipenser oxyrinchus '' ) .
Dane genetyczne sugerują , że jesiotr zachodni nigdy nie tworzył w Bałtyku populacji o samowystarczalnej liczebności .
Cechy morfologiczne .
Grzbiet jest niebieskoszary lub szarozielony , pokryty rzędem 10-13 jasnych tarczek kostnych , które u młodych osobników zwężają się często w kolce .
Na srebrzystych bokach występuje od 24 do 40 tarczek , na białym brzuchu 10-13 .
Pysk jest spiczasty , wydłużony .
Otwór gębowy prawie kwadratowy , zajmuje niemal 2/3 szerokości pyska .
Wąsiki są pozbawione przydatków i okrągłe w przekroju .
Pierwszy promień płetwy piersiowej jest bardzo gruby .
Jesiotr zachodni zazwyczaj osiąga długość 1,5-2,5 m , rekordowo do 4 m i 300 kg masy .
Typ łuski ganoidalna .
