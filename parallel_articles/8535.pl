Unia Europejska
Unia Europejska , UE ( ang .
`` European Union '' , EU ) – gospodarczo-polityczny związek 27 demokratycznych państw europejskich .
Unia powstała 1 listopada 1993 roku na mocy podpisanego 7 lutego 1992 traktatu z Maastricht – jako efekt wieloletniego procesu integracji politycznej , gospodarczej i społecznej .
Korzenie współczesnej integracji europejskiej sięgają okresu powojennego i ograniczały się do 6 państw zachodnioeuropejskich .
Państwa te tworzyły wiele form i mechanizmów współpracy , powoływały organizacje , instytucje i organy , których celem było wzmocnienie jedności między nimi .
W 1993 nadrzędną wobec wszystkich poprzednich organizacji została Unia Europejska , sama otrzymując nieznaną wcześniej hybrydową formułę `` sui generis '' .
Unia Europejska jako organizacja międzynarodowa ( w międzynarodowoprawnym tego słowa znaczeniu ) funkcjonuje od 1 grudnia 2009 .
Równocześnie zastąpiła ona kilka form współpracy , w tym m.in .
