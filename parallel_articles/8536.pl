Konkurs Piosenki Eurowizji dla Dzieci 2014
12 .
Konkurs Piosenki Eurowizji dla Dzieci odbył się 15 listopada 2014 roku na terenie byłej stoczni Malta Shipbuilding w Marsie ( niedaleko Valletty ) na Malcie .
Był to pierwszy konkurs Europejskiej Unii Nadawców ( EBU ) , który odbył się w tym kraju oraz trzeci w historii Konkurs Piosenki Eurowizji dla Dzieci organizowany w państwie triumfującym rok wcześniej .
Gospodarzem wydarzenia był maltański nadawca telewizyjny , Public Broadcasting Services ( PBS ) .
Konkurs poprowadziła aktorka i prezenterka telewizyjna Moira Delia .
Lokalizacja .
Wybór miejsca organizacji .
Po zwycięstwie Gai Cauchi , reprezentantki Malty rok wcześniej , Europejska Unia Nadawców zaproponowała organizację konkursu maltańskiej telewizji publicznej .
