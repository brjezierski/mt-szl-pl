Oklahoma City
Oklahoma City – miasto w Stanach Zjednoczonych nad rzeką North Canadian , stolica stanu Oklahoma .
W 2021 roku obszar metropolitalny Oklahoma City obejmuje 1,44 mln mieszkańców .
Ośrodek handlu i bankowości .
Ważny węzeł kolejowy krzyżujących się linii z północy na południe i ze wschodu na zachód .
Do XXI wieku miasto było jednym z największych rynków bydła na świecie .
Wiele firm nadal świadczy usługi i dostawy dla rolników , hodowców i kowbojów .
Historia .
Założone 22 kwietnia 1889 dla białych osadników .
Prawa miejskie uzyskało w 1890 r. , a od 1910 r. jest stolicą stanu .
Miasto rozwinęło się jako punkt dystrybucji zbóż i bydła .
W 1928 r. w pobliżu miasta odkryto i rozpoczęto eksploatację złóż ropy naftowej i gazu ziemnego .
