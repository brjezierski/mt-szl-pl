Oktoberfest
Oktoberfest ( baw .
`` Wiesn '' ) – dożynki chmielne organizowane w Monachium od 1810 roku i zarazem jeden z największych festynów ludowych na świecie – w ostatnich latach przeciętna liczba odwiedzających wynosiła ok. 6 milionów ludzi .
Do roku 1871 odbywał się w październiku , natomiast od roku 1872 początek imprezy został przesunięty na wrzesień .
W trakcie trwania festynu spożywane jest około 5 milionów litrów piwa , sprzedawanego tradycyjnie w litrowych kuflach tzw .
Maßkrug .
Historia .
Festyny nazywane Oktoberfestem nie były w Bawarii rzadkością .
Ich celem było zużycie piwa z mijającego sezonu browarskiego , zanim rozpoczęto warzyć nowe .
Ponieważ Bawarskie Prawo Czystości z 1516 roku zezwalało na warzenie piwa tylko od 29 września do 23 kwietnia , przypadały tego typu festyny właśnie na okres przełomu września i października .
Monachijski Oktoberfest cieszy się już ponad 200-letnią tradycją .
Z okazji ślubu bawarskiego księcia Ludwika , późniejszego króla Bawarii i jego wybranki , księżniczki Therese von Sachsen-Hildburghausen , który odbył się 12 października 1810 , zorganizowane zostały na łąkach przed murami miasta ( dzisiejsza Theresienwiese , „ Błonia Teresy ” ) wyścigi konne .
Ponieważ książę Ludwik pasjonował się starożytnością zawody odbyły się w stylu starożytnych igrzysk olimpijskich .
W początkowych latach monachijski Oktoberfest miał przede wszystkim sportowy charakter .
Ponieważ impreza cieszyła się dużą popularnością wśród mieszkańców miasta , dwór królewski zadecydował o powtórzeniu wyścigów rok później o tej samej porze i tak zrodziła się tradycja monachijskiego Oktoberfestu .
Ze względu na uwikłanie Bawarii w wojnach napoleońskich w 1813 roku po raz pierwszy zrezygnowano z festynu .
Przerwy w tradycji nastąpiły również w 1854 i 1873 ze względu na epidemię cholery , w 1866 roku z powodu wojny siedmiotygodniowej , w 1870 wojny francusko-pruskiej i w latach 2020–2021 z powodu pandemii koronawirusa .
Piwo i tradycje piwne .
Pod koniec XIX wieku monachijski Oktoberfest nabierał coraz bardziej charakteru jaki ma dzisiaj .
W 1880 roku władze miasta zezwoliły na sprzedaż piwa .
Wedle przepisów Oktoberfestu swoje piwo serwować mogą jedynie monachijskie browary , które warzą zgodnie z Bawarskim Prawem Czystości z 1487 roku i podobną normą niemiecką z 1906 roku .
Do browarów tych należą :
Wszystkie one oferowały tradycyjne bawarskie piwo marcowe o zawartości alkoholu do 6,2 % .
Charakteryzuje się ono bursztynową barwą i przewagą słodkich nut słodowych nad goryczką chmielową .
Obecnie piwa serwowane na festynie nazywa się `` Oktoberfestbier '' i w stylu są bardziej podobne do popularnego w wielkich koncernach lagera niż do dawnego piwa marcowego .
Dla odwiedzających ustawianych jest 14 hal piwnych , z których większe mają do 10 tysięcy miejsc siedzących .
Hale dzielone są na dwie kategorie , hale browarów i namioty gospodarzy Oktoberfestu .
Każdy gospodarz sprzedaje w swoim namiocie piwo tylko jednego browaru .
W 2014 r. jedno piwo kosztowało średnio dziesięć euro .
