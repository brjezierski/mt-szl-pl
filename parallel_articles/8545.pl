Okuninka
Okuninka – wieś w Polsce położona w województwie lubelskim , w powiecie włodawskim , w gminie Włodawa .
W latach 1975–1998 miejscowość administracyjnie należała do województwa chełmskiego .
Miejscowość letniskowa na Pojezierzu Łęczyńsko-Włodawskim , położona nad Jeziorem Białym .
Od lat sześćdziesiątych budowano w niej ośrodki wypoczynkowe , które dziś z trzech stron zajmują brzeg Jeziora Białego , ich liczba przekracza 40 .
Ponadto jest tam kilka dyskotek , kilkadziesiąt restauracji i barów , a w okresie letnim – wesołe miasteczko .
Sama wieś położona jest na północnym brzegu jeziora , zajmuje się głównie agroturystyką .
Przy Okunince znajdują się również jeziora : Lipiec , Święte , Rogoźne i Czarne , a w odległości kilkuset metrów jezioro Glinki .
Jezioro Białe ma I klasę czystości i wraz z turystycznym zapleczem Okuninki co roku latem przyciąga kilkadziesiąt tysięcy ludzi .
Charakterystyczne budynki to kościół , restauracja Rusałka i jeden z pierwszych obiektów letniskowych – camping na północnym brzegu jeziora .
Wierni Kościoła rzymskokatolickiego należą do parafii Najświętszego Serca Jezusowego we Włodawie .
We wsi jest kościół filialny .
