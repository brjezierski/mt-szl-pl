Olaf Lubaszenko
Olaf Lubaszenko , właściwie Olaf Sergiusz Linde ( ur .
6 grudnia 1968 we Wrocławiu ) – polski aktor filmowy i teatralny , reżyser i producent , prezenter telewizyjny , także wokalista i konferansjer , związany z Teatrem Buffo , dyrektor artystyczny Festiwalu Gwiazd w Międzyzdrojach , laureat Orła i Czeskiego Lwa za pierwszoplanową rolę w filmie `` Zabić Sekala '' .
Od 1 września 2022 dyrektor Teatru im .
Adama Mickiewicza w Częstochowie .
Życiorys .
Wczesne lata .
Jest synem aktora Edwarda Linde-Lubaszenki oraz aktorki , plastyczki i poetki Asji Łamtiuginy .
Ma przyrodnią siostrę Beatę .
Jego ojcem chrzestnym został Jan Nowicki .
Uczęszczał do Szkoły Podstawowej nr 9 w Białymstoku ( matka była aktorką tamtejszego teatru ) .
Jest absolwentem IX LO im .
