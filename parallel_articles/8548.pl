Olbernhau
Olbernhau – miasto w Niemczech , w kraju związkowym Saksonia , w powiecie Erzgebirgskreis .
Do 29 lutego 2012 w okręgu administracyjnym Chemnitz .
Historia .
Miejscowość została założona przez cystersów przybyłych z pobliskiego czeskiego Oseku .
Najstarsza zachowana wzmianka o miejscowości `` Albernaw '' pochodzi z 1434 roku .
Od XV-XVI wieku w Olbernhau i okolicy rozwijało się hutnictwo , a od ok. 1684 rusznikarstwo .
W 1621 w pobliskiej miejscowości Grünthal powstała mennica , będąca filią mennicy drezdeńskiej .
Zakończyła działalność w 1623 , by wznowić ją w latach 50 .
XVIII wieku , kiedy bito tu polskie szelągi króla Augusta III ( miejscowość była wówczas częścią unijnego państwa polsko-saskiego ) .
W 1815 w Olbernhau rozpoczęto produkcję prochu strzelniczego .
Fabryka prochu uległa częściowemu zniszczeniu w eksplozjach lat 1835 , 1850 , 1865 .
W 1871 roku Olbernhau zostało częścią Niemiec , a w 1875 uzyskało połączenie kolejowe .
W kolejnych latach zbudowane zostały przedszkole i szpital , a w 1892 uruchomiono tu pierwszą elektrownię w Saksonii .
W 1902 Olbernhau otrzymało prawa miejskie .
W 1937 Grünthal włączono w granice miasta .
W 1950 roku do miasta przyłączono Oberneuschönberg , Niederneuschönberg i Kleinneuschönberg , a w 1994 Blumenau i Rothenthal .
1 stycznia 2017 do miasta przyłączono gminę Pfaffroda , która stała się tym samym jego dzielnicą .
