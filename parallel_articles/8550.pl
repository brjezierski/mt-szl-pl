Oldham
Oldham – miasto w północno-zachodniej Anglii , w hrabstwie Wielki Manchester , położone na północno-wschodnim skraju konurbacji Manchesteru .
Ośrodek administracyjny dystryktu metropolitalnego o tej samej nazwie .
W 2001 roku miasto liczyło 103 544 mieszkańców Miasto zamieszkane jest przez liczącą około 26 tysięcy ludzi mniejszość azjatycką , zwłaszcza z Bangladeszu , Pakistanu i Indii .
Pierwsze wzmianki o mieście pochodzą z XI wieku .
W mieście ważną rolę odgrywa przemysł tekstylny .
Oldham położone jest około 5 kilometrów od autostrady krajowej M62 .
Obecnie w mieście funkcjonują dwie stacje kolejowe .
Miasto jest połączone z siecią tramwajową Metrolink , obsługującą transport publiczny w konurbacji Manchesteru .
W mieście rozwinął się przemysł maszynowy , elektroniczny , metalowy oraz włókienniczy .
