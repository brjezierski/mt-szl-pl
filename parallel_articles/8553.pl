Prezerwatywa
Prezerwatywa ( anglicyzm `` kondom '' ) – mechaniczny środek antykoncepcyjny .
Prezerwatywa może być stosowana przez :
Prezerwatywa zmniejsza prawdopodobieństwo zakażenia się chorobami przenoszonymi drogą płciową ( np .
: kiłą , rzeżączką , czy zakażeniem się wirusem : HIV , HBV oraz HCV ) .
Jednak tylko częściowo zmniejsza prawdopodobieństwo zakażenia się wirusem HPV .
Skuteczność prezerwatywy w zapobieganiu przenoszenia wirusa HIV podczas stosunków heteroseksualnych oceniana jest na 80-95 % .
Jak zaznacza WHO : `` To oznacza , że używanie prezerwatyw chroni przed 80 % do 95 % zakażeń wirusem HIV , które pojawiłyby się , gdyby prezerwatywy nie zostałyby użyte ( nie znaczy to , że 5 % do 20 % użytkowników prezerwatyw zakazi się HIV ) '' .
Historia prezerwatywy .
Prezerwatywy są jednym z najstarszych znanych sposobów antykoncepcyjnych .
Dawniej wytwarzane były z jelit zwierzęcych , dzisiaj produkuje się je z lateksu ( rzadziej z silikonu ) .
Egipcjanie używali prezerwatyw w roku 1350 p.n.e .
jako osłonek na penisy .
Prezerwatywa stała się popularną ochroną przed niechcianą ciążą w XVIII wieku .
Breve papieskie z 1826 r. będące stanowiskiem Kościoła katolickiego oceniało prezerwatywę negatywnie i potępiało jej stosowanie .
Pierwsza prezerwatywa lateksowa została wyprodukowana w 1912 roku przez Juliusa Fromma .
Zakładanie prezerwatywy męskiej .
Aby bezpiecznie założyć prezerwatywę należy upewnić się czy opakowanie nie jest uszkodzone .
Łapie sią ją za zbiorniczek na końcu i rozwija na członku .
Założona prezerwatywa naciągnięta jest na całą długość penisa , zazwyczaj część zostaje zrolowana .
Na jej końcu zwykle znajduje się wypustka , która wypełni się nasieniem w czasie wytrysku .
Dzięki temu plemniki znajdujące się w nasieniu nie dostaną się do pochwy partnerki .
Wady i zalety .
W dzisiejszych czasach prezerwatywa , oprócz swych podstawowych funkcji , ma za zadanie urozmaicenie życia seksualnego .
W tym celu producenci tworzą : prezerwatywy smakowe ( przeznaczone do fellatio ) , kolorowe , z różnego rodzaju wypustkami ( mającymi zwiększyć doznania partnerki/partnera ) .
Do wielu także dodaje się specjalne środki nawilżające , plemnikobójcze lub opóźniające wytrysk ( dla mężczyzn z tendencją do przedwczesnego orgazmu ) .
Zaletami prezerwatyw jest także dostępność ( niepotrzebna jest recepta ) i możliwość natychmiastowego użycia bez skomplikowanych zabiegów .
Używanie ich , podobnie jak innych środków zapobiegania zapłodnieniu , spotyka się z krytyką ze strony niektórych wyznań religijnych .
