Olivier Giroud
Olivier Jonathan Giroud ( ur .
30 września 1986 w Chambéry ) – francuski piłkarz , występujący na pozycji napastnika we włoskim klubie AC Milan oraz w reprezentacji Francji .
Złoty medalista Mistrzostw Świata 2018 , srebrny medalista Mistrzostw Świata 2022 i Mistrzostw Europy 2016 , uczestnik Mistrzostw Europy 2012 , 2020 i Mistrzostw Świata 2014 .
Kariera klubowa .
Początki .
Olivier Giroud podstaw futbolu uczył się w klubie z rodzinnej wioski w Alpach francuskich – Olympique Froges .
W wieku 13 lat znalazł się w drużynie juniorów pobliskiego Grenoble Foot 38 , a w wieku 19 lat podpisał pierwszy zawodowy kontrakt z tym klubem .
Po kilku sezonach w rezerwach Grenoble , w których pokazywał się z dobrej strony , otrzymał parę szans w seniorskiej kadrze występującej w Ligue 2 .
Jako , że nie przebił się do końca w Grenoble , postanowił odejść na wypożyczenie do FC Istres grającego w Championnat National , czyli 3. klasie rozgrywkowej , a więc stopień niżej .
Tours FC .
Po sezonie 2007/2008 , w którym zdobył 14 bramek dla Istres , został zauważony przez Tours FC z Ligue 2 .
