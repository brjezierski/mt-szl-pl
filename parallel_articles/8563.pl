Olten
Olten – miasto w północnej Szwajcarii ( Solura ) , nad rzeką Aare .
Liczba mieszkańców w ( 2005 ) roku wynosiła 16 801 , w 2017 - 19 148 .
Ośrodek przemysłowy ( przemysł maszynowy , środków transportu , chemiczny i cementowy ) .
W mieście znajduje się węzeł kolejowy ze stacją Olten .
Znajdują się tam też cztery muzea : archeologiczne , historyczne , przyrody i sztuki oraz teatr z salą koncertową i dwie sceny teatralne .
W pobliżu Olten usytuowana jest elektrownia wodna na rzece Aere .
