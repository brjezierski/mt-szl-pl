Oléron
Oléron ( fr .
Île d ’ Oléron ) – wyspa francuska położona przy atlantyckim wybrzeżu Francji , na zachód od Rochefort , po południowej stronie cieśniny Pertuis d'Antioche .
Druga pod względem powierzchni , po Korsyce , największa wyspa Francji ( nie uwzględniając posiadłości zamorskich ) .
Wyspa od 1966 roku jest połączona ze stałym lądem mostem o długości 3027 m. Jest to najdłuższy most we Francji .
Od 1991 roku nie pobiera się na nim opłat za przejazd .
Wyspa ma 34 km długości i 12 km szerokości .
Zajmuje obszar 175 km² i liczy 19 000 stałych mieszkańców .
Podział administracyjny .
Administracyjnie wyspa należy do departamentu to the Charente-Maritime , w regionie Poitou-Charentes .
