Omar Bongo
El Hadj Omar Bongo Ondimba ( ur .
jako Albert-Bernard Bongo 30 grudnia 1935 w Lewai , zm .
8 czerwca 2009 w Barcelonie ) – gaboński polityk , minister , wiceprezydent w latach 1966–1967 , prezydent Gabonu od 1967 do 2009 .
Rządził przez 42 lata .
Jego następcą został wybrany syn , Ali Bongo Ondimba .
Omar Bongo , człowiek niskiej postury , po ukończeniu służby wojskowej rozpoczął pracę w administracji publicznej .
W latach 60 .
XX w. , jako młody urzędnik , objął kluczowe stanowiska w administracji prezydenta Léona M ’ by .
