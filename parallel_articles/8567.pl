Omdurman
Omdurman ( أم درمان ) – największe miasto w Sudanie ( ponad 1,2 mln mieszkańców w 1993 , 2,8 mln 1 stycznia 2005 ) .
Położone na lewym brzegu Nilu Białego , naprzeciw stolicy państwa , Chartumu .
Połączone z Chartumem i Chartumem Północnym mostami , stanowi wraz z nimi przemysłowe , handlowe i kulturalne centrum państwa .
W 1884 Muhammad Ahmad ibn as-Sayyid Abd Allah , zwany `` Mahdim '' , w czasie walk powstania przeciw Brytyjczykom uczynił wieś Omdurman główną kwaterą swych wojsk .
Po zdobyciu Chartumu w 1885 Omdurman został stolicą kraju .
Mahdi zmarł w 1885 i został pochowany w Omdurmanie .
Ten fakt jest wspomniany w książce Henryka Sienkiewicza „ W pustyni i w puszczy ” .
2 września 1898 miała miejsce bitwa pod Omdurmanem , w której Lord Kitchener rozbił siły mahdystów , co doprowadziło do upadku powstania .
W bitwie tej większa część miasta została zniszczona , jednak grób Mahdiego został odrestaurowany i istnieje do dziś .
