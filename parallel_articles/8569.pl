Omphalotus olearius
Omphalotus olearius ( DC . )
Singer– gatunek grzybów z rodziny `` Omphalotaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Omphalotaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1815 r. Augustin Pyramus de Candolle nadając mu nazwę `` Agaricus olearius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1946 r. Rolf Singer .
Niektóre synonimy :
W niektórych atlasach grzybów opisywany jest jako kielichowiec pomarańczowy .
Charakterystyka .
Grzyb trujący o lejkowatym kształcie , intensywnie świecący w ciemności .
Światło wydzielane przez ten grzyb ma kolor zielony .
Jest to grzyb ciepłolubny .
Występuje na południu Europy , poczynając od południowych Czech i południowej Słowacji .
W Polsce nie występuje .
Rośnie na pniakach dębów , kasztanowców i oliwek .
