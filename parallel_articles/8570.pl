Omsk
Omsk ( ) – miasto w Rosji , na Nizinie Zachodniosyberyjskiej , przy ujściu rzeki Om do Irtyszu .
Stolica obwodu omskiego .
Historia .
Osada założona w 1716 roku jako warownia dla obrony przed Kirgizami , potem zespół faktorii handlowych .
Prawa miejskie w latach 1782–1797 i od 1804 r. W mieście od 1822 znajdowała się siedziba generał-gubernatora Syberii Zachodniej .
Na początku XX wieku wielkie centrum handlowe .
Od 18 listopada 1918 do 10 listopada 1919 siedziba antybolszewickiego rządu Białych pod zwierzchnictwem admirała Aleksandra Kołczaka .
W okolicach Omska znajdują się kolonie karne .
Obecnie miasto jest ważnym ośrodkiem przemysłowym , naukowym ( 10 wyższych uczelni w tym uniwersytet założony w 1974 r. ) i kulturalnym ( muzea , teatr z 1874 ) .
