Onseepkans
Onseepkans ( wymowa afrikaans : [ ] ) - miejscowość w Republice Południowej Afryki , przy granicy z Namibią , w Prowincji Przylądkowej Północnej , nad rzeką Oranje , 57 kilometrów na północ od Pofadder oraz 107 km na południowy wschód od Karasburga , leżącego już w granicach administracyjnych Namibii .
Nazwa miejscowości wywodzi się z języka Hotentotów .
Onseepkans założone zostało w 1916 roku przez osadników-misjonarzy na terenach do dziś nawadnianych sztucznie wodą z rzeki Oranje ( same zabudowania mieszkalne stoją bowiem na terenach pustynnych ) .
Po obu stronach rzeki znajdują się małe osiedla .
Pomiędzy Onseepkans a Pofadder rozciąga się las , w którym przeważają drzewa kołczanowe .
W 1965 roku populacja wynosiła nie więcej jak tysiąc mieszkańców .
