Opelousas
Opelousas - miasto w Stanach Zjednoczonych , w południowo-środkowej części stanu Luizjana .
Około 23 tys .
mieszkańców .
Historia .
Założone w 1720 roku jest trzecim najstarszym miastem Luizjany .
Swą nazwę bierze od plemienia indiańskiego Appalousa .
W XVIII i XIX wieku miasto służyło jako główny punkt handlowy pomiędzy Nowym Orleanem i Natchitoches .
Jest nazywane `` światową stolicą przypraw '' z powodu ich produkcji na szeroką skalę .
