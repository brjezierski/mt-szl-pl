Maczużnik osi ( `` Ophiocordyceps sphecocephala '' ( Klotzsch ex Berk . )
G.H .
Sung , J.M .
Sung , Hywel-Jones & amp ; Spatafora ) – gatunek grzybów z rodziny `` Ophiocordycipitaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ophiocordyceps , Ophiocordycipitaceae , Hypocreales , Hypocreomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozowali go w 1843 r. Johann Friedrich Klotzsch i Miles Joseph Berkeley , nadając mu nazwę `` Sphaeria sphecocephala '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu G.H .
Sung , J.M .
Sung , Hywel-Jones i Spatafora w 2007 r .
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda , wówczas gatunek ten zaliczany był do rodzaju `` Cordyceps '' ( maczużnik ) .
Według Index Fungorum należy jednak do rodzaju `` Ophiocordyceps '' , stąd też nazwa polska jest już niespójna z nazwą naukową .
Ponadto nazwę maczużnik w 2003 r. Wiesław Fałtynowicz podał dla innego rodzaju grzybów – `` Sphinctrina '' .
Synonimy naukowe :
Morfologia i tryb życia .
Po zainfekowaniu owada grzybnia rozrasta się w jego ciele , powodując specyficzne zachowanie owada .
Porażony owad wznosi się na najwyższe miejsce na roślinie i umiera , trzymając się jej .
Na martwym ciele owada grzyb tworzy maczugowatą podkładkę o wysokości do 6 cm .
Składa się ona z żółtawej główki i długiego , białawego trzonu .
Występowanie .
Występuje w Ameryce Północnej , Środkowej i Południowej , Europie , Azji i Afryce .
Występuje także w Polsce .
Jest rzadki .
Jego aktualne stanowiska podaje internetowy atlas grzybów .
Znajduje się w nim na liście gatunków zagrożonych , wartych objęcia ochroną .
Maczużnik osi to grzyb entomopatogeniczny będący pasożytem os i innych błonkówek , zwłaszcza z rodzajów `` Polistes '' , `` Tachytes '' i `` Vespa '' .
Znaczenie .
Podobnie jak inne gatunki maczużników , `` Ophiocordyceps sphecocephala '' zawiera aktywne biologicznie substancje , badane pod kątem przydatności w medycynie , przede wszystkim w terapii nowotworów .
Jest poszukiwany przez chińskich lekarzy i w handlu osiąga wysoką cenę .
