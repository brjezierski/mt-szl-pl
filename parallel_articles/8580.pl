Ophiostoma novo-ulmi
Ophiostoma novo-ulmi Brasier – gatunek grzybów z rodziny `` Ophiostomataceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ophiostoma , Ophiostomataceae , Ophiostomatales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Cykl rozwojowy .
`` Ophiostoma novo-ulmi '' to pasożyt obligatoryjny przenoszony przez chrząszcze ogłodka wiązowca ( `` Scolytus scolytus '' ) i ogłodka wielorzędowego ( `` Scolytus multistriatus '' ) .
Żerują one pod korą osłabionych wiązów oraz ich ściętych pni , tworząc tam korytarze .
W korytarzach tych znajdują się zarodniki grzyba .
