Oppersdorffowie
Oppersdorffowie – śląski ród szlachecki i arystokratyczny .
Od 1554 r. baronowie cesarstwa , od 1626 r. hrabiowie cesarstwa .
Dzieje rodu .
Pierwszy tytuł arystokratyczny – baronów cesarstwa z przydomkiem „ von Aich und Friedstein ” - został nadany trzem braciom : Hansowi , Jerzemu I i Wilhelmowi w Wiedniu 21 kwietnia 1554 r .
Najstarszy z braci Hans ( zm .
1584 r. ) poprzez małżeństwo z Magdaleną von Zedlitz nabył prawa do zamku Głogówek i okolicznych dóbr .
Od 1561 r. posiadał je na prawie dzierżawy .
Po jego bezpotomnej śmierci majątek odziedziczył syn Jerzego I , Jerzy II ( zm .
1606 r. ) , który w 1595 r. stał się dziedzicznym właścicielem dóbr .
Był radcą cesarskim i starostą księstwa opolsko-raciborskiego .
Jerzy II wraz z bratem Fryderykiem II zostali uznani 12 lutego 1601 r. za czeskich baronów .
Syn Jerzego II , Jerzy III został hrabią cesarstwa na podstawie przywileju wystawionego w Wiedniu 22 czerwca 1626 r. Bracia Jerzego III , Fryderyk i Wacław tytułu hrabiów cesarstwa otrzymali 30 kwietnia 1635 r .
Jerzy III z dóbr głogóweckich utworzył 20 lutego 1642 r. fideikomis .
Był cesarskim radcą i podskarbim oraz starostą opolsko-raciborskim .
Około 1634 r. hrabia Jerzy III wybudował w Głógówku kaplicę Grobu Chrystusa .
W 1636 hrabia Jerzy wykupił frydeckie państwo stanowe .
W czasie wojny polsko-szwedzkiej w latach 1655-1660 r. ( tzw .
Potop szwedzki ) hrabia Franciszek Euzebiusz Oppersdorff udzielił na swym zamku schronienia polskiemu królowi Janowi II Kazimierzowi Wazie i jego małżonce Ludwice Marii .
Monarcha z małżonką przebywali w Głogówku od 17 października do 18 grudnia 1655 r. Podczas obrad sejmu w 1683 , Franciszek Graf von Oppersdorff otrzymał indygenat na polskie szlachectwo wraz z herbem Kur .
Został w ten sposób uhonorowany za zasługi położone podczas wojny ze Szwedami , która toczyła się za panowania króla Jana II Kazimierza .
Głogówek do 1714 r. należał do linii śląskiej Oppersdorffów , następnie do 1781 r. do linii morawskiej rodu , a następnie czeskiej .
Linia czeska używała tytułu hrabiów już od 1700 r. Tytuł ten został uznany na Morawach i Śląsku w 1746 r .
W czasie podboju Europy przez cesarza Napoleona Bonaparte , uciekając przez francuskim władcą na zamku w 1806 r. schronił się Ludwig van Beethoven .
Słynny kompozytor zadedykował swemu gospodarzowi - hrabiemu Franciszkowi - swą IV symfonię .
Pan na zamku Głogówek był od 12 października 1854 r. członkiem pruskiej Izby Panów .
Fideikomis głogówiecki został rozwiązany 21 marca 1935 r .
