Oradea ( stacja kolejowa )
Oradea ( rum : `` Gara Oradea '' ) – stacja kolejowa w Oradei , w Okręgu Bihor , w Rumunii .
Stacja jest obsługiwana przez pociągi Căile Ferate Române ( CFR ) .
Znajduje się na ważnej magistrali kolejowej Bukareszt – Oradea , która dalej biegnie do Budapesztu .
W Budynku dworca funkcjonuje poczekalnia , posterunek policji , toaleta .
Przed budynkiem dworca znajduje się postój taksówek oraz przystanki komunikacji publicznej .
