Masakra w Oradour .
10 czerwca 1944 r. oddział niemieckich wojsk Waffen-SS wchodzący w skład 2 Dywizji Pancernej SS „ Das Reich ” pod dowództwem SS-Sturmbannführera Adolfa Diekmanna wymordował niemal całą ludność miasteczka , w odwecie za zabicie przez `` Resistance '' oficera SS Helmuta Kämpfe .
Zamordowano 642 osoby – mężczyzn , kobiety i dzieci .
Niemcy wprowadzili mężczyzn do stodół , a następnie rozstrzelali , a kobiety i dzieci zamknęli w kościele , który podpalili .
Żołnierze wrzucali przez okna granaty , a próbujących uciec zabijali seriami z broni maszynowej .
Przeżyła tylko jedna kobieta ( 47-letnia Marguerite Rouffanche , która wyskoczyła przez małe okienko w kościele ) , pięciu mężczyzn oraz dziecko .
W dodatku spacyfikowano nie tę miejscowość – oficera SS zabito w Oradour-sur-Vayres .
Ruiny Oradour-sur-Glane pozostawiono w oryginalnym stanie .
Przy wejściu znajduje się tablica z napisem • ( „ Pamiętaj ” ) ( dwujęzyczne tablice o tej treści umieszczono po wojnie przy nazwie miasta na tablicach drogowych przy wjeździe do miasta ) .
W 1999 r. przy ruinach otwarte zostało Centre de la mémoire d'Oradour .
Na kanwie tych wydarzeń powstał film `` Stara strzelba '' .
