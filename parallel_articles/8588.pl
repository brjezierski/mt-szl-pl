Pieprznik pomarańczowy
Pieprznik pomarańczowy , pieprznik Friesa ( `` Cantharellus friesii '' Quél . )
– gatunek grzybów z rodziny kolczakowate ( `` Hydnaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Cantharellus , Hydnaceae , Cantharellales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim naukowy – `` Merulius friesii '' ( Quél . )
Kuntze .
Polską nazwę pieprznik pomarańczowy podał Władysław Wojewoda w 2003 r. W 1995 r. Terpińska-Ostrowska opisywała ten gatunek pod nazwą pieprznik Friesa .
Drugi człon tej nazwy jest tłumaczeniem nazwy łacińskiej nadanej na cześć szwedzkiego botanika Eliasa Magnusa Friesa .
Morfologia .
Średnicy 1–4 cm .
Jaskrawopomarańczowy , żółtopomarańczowy lub czerwonopomarańczowy .
Za młodu wypukły , potem spłaszczony , u dojrzałych owocników lejkowaty z powyginanym , podwiniętym i kędzierzawym brzegiem .
Powierzchnia kapelusza gładka lub lekko aksamitna .
Grube , w formie nieregularnie rozwidlonych listewek , szeroko rozstawione i daleko zbiegające .
