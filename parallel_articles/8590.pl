Guziczka brodawkowanozarodnikowa `` Orbilia xanthostigma '' ( Fr . )
Fr .
– gatunek grzybów workowych .
Charakterystyka .
U przedstawicieli rodzaju `` Orbilia '' znane są tylko formy rozmnażające się płciowo ( teleomorfy ) .
Nie tworzą podkładek , lecz na grzybni od razu owocniki typu apotecjum o barwie od żółtej do pomarańczowej i średnicy 1–2 mm .
Askospory małe ( zwykle poniżej 10 × 1 μm ) , hialinowe i owalne lub elipsoidalne .
Wiele gatunków ma parafizy zakończone guzkami i worki z widocznymi połączeniami strzępek .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Orbiliaceae , Orbiliales , Orbiliomycetidae , Orbiliomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał Elias Fries w 1815 r. nadając mu nazwę `` Peziza xanthostigma '' .
