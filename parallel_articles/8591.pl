Orbit
Orbit – marka bezcukrowych gum do żucia firmy Wrigley Company .
Dostępna jest w różnych smakach i opakowaniach .
Guma Orbit powstała w Stanach Zjednoczonych w 1944 .
Podczas II wojny światowej , ze względu na racjonowanie składników , skład gumy był uproszczony .
Nazwa Orbit powstała ze słowa `` sorbitol '' ( alkohol cukrowy ) .
Pod koniec 2007 roku , American Dental Association wydało oświadczenie , że żucie niektórych gum Orbit , przez co najmniej 20 minut po jedzeniu , poprawia zdrowie jamy ustnej .
