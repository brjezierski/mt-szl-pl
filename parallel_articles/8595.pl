Orem
Orem – miasto ( `` city '' ) w hrabstwie Utah , w północnej części stanu Utah , w Stanach Zjednoczonych , położone pomiędzy jeziorem Utah na zachodzie a rzeką Provo na wschodzie , w sąsiedztwie miasta Provo , z którym tworzy zespół miejski .
W 2013 roku miasto liczyło 91 648 mieszkańców .
Miasto założone zostało w 1919 roku .
Jego nazwa pochodzi od Waltera Orema , magnata kolejowego .
