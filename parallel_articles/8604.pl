Orkdal
Gmina Orkdal ( ) – norweska gmina leżąca w okręgu Trøndelag .
Jej siedzibą jest miasto Orkanger .
Orkdal jest 185. norweską gminą pod względem powierzchni .
Demografia .
Według danych z roku 2005 gminę zamieszkuje 10 512 osób .
Gęstość zaludnienia wynosi 17,72 os./km² .
Pod względem zaludnienia Orkdal zajmuje 98. miejsce wśród norweskich gmin .
