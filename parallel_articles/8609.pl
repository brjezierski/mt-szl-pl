Ormosbánya
Ormosbánya – wieś i gmina w północno-wschodniej części Węgier , w pobliżu miasta Kazincbarcika .
Miejscowość leży na obszarze Średniogórza Północnowęgierskiego , niedaleko granicy ze Słowacją .
Administracyjnie gmina należy do powiatu Kazincbarcika , wchodzącego w skład komitatu Borsod-Abaúj-Zemplén i jest jedną z jego 33 gmin .
