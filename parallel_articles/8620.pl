Osaka ( zatoka )
– zatoka w zachodniej części wyspy Honsiu ( `` Honshū '' ) , w Japonii .
Stanowi wschodnią część Morza Wewnętrznego ( `` Seto-naikai '' ) i jest połączona z Oceanem Spokojnym przez cieśninę Kitan oraz kanał Kii , a z Morzem Wewnętrznym przez cieśninę Akashi .
Nad zatoką leżą m.in .
miasta portowe : Osaka , Kobe , Nishinomiya , Sakai , Amagasaki , Kishiwada .
Na zatoce utworzono wiele sztucznych wysp m.in .
dla portu i lotniska w Kobe oraz dla lotniska Kansai .
