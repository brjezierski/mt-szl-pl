Oskarshamn ( stacja kolejowa )
Oskarshamn ( szwedzki : `` Oskarshamns järnvägsstation '' ) – stacja kolejowa w Oskarshamn , w regionie Kalmar , w Szwecji .
Budynek został wzniesiony w 1906 roku w stylu renesansu nordyckiego .
Architektura budynku jest również opisywana jako mieszanina funkcjonalizmu , secesji i romantyzmu narodowego .
Budynek dworca położony jest blisko portu i terminala promowego , obsługującego ruch na Gotlandię .
