Osnabrück
Osnabrück ( dolnoniem .
`` Ossenbrügge '' ) – miasto na prawach powiatu w Niemczech , w kraju związkowym Dolna Saksonia , położone w północno-zachodniej części kraju , siedziba powiatu Osnabrück , do którego jednak nie należy .
Położenie .
Osnabrück leży nad rzeką Hase w regionie Osnabrücker Land , między północną częścią Lasu Teutoburskiego i gór Wiehengebirge .
Około 80 km na północ od Dortmundu , ok. 45 km od Münster i ok. 100 na zachód od Hanoweru .
Toponimika nazwy .
Jest kilka interpretacji nazwy Osnabrück i jej pochodzenia .
Pierwsza część „ Osna- ” może wiązać się z dolnoniemieckim „ Osse ” ( wół ) , lub „ Hase ” – nazwą rzeki przepływającej przez historyczną część miasta .
Może mieć też związek ze starą nazwą Lasu Teutoburskiego `` Osning '' .
Druga część nazwy miasta – „ Brück ” oznacza most .
