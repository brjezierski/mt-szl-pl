Osorno
Osorno – miasto w Chile , w regionie Los Lagos , przy autostradzie panamerykańskiej , około 100 km na północ od Puerto Montt .
Stolica prowincji Osorno .
W 2013 r. miasto liczyło 162 238 mieszkańców .
Osorno zostało założone w 1568 roku , lecz dwukrotnie uległo zniszczeniu w wyniku konfliktu Chile z Araukanami .
W mieście rozwinął się przemysł spożywczy oraz drzewny .
