Osowa Sień
Osowa Sień ( niem .
`` Röhrsdorf '' ) – wieś w Polsce położona w województwie lubuskim , w powiecie wschowskim , w gminie Wschowa
Wieś położona 4 km na pn.-wsch .
od Wschowy .
Wieś jest siedzibą sołectwa `` Osowa Sień '' , w którego skład wchodzi również miejscowość Wincentowo .
25 czerwca 2009 roku sołectwo zamieszkiwało 1125 mieszkańców .
W latach 1975–1998 miejscowość była położona w województwie leszczyńskim .
Etymologia .
Obecna nazwa miejscowości oznacza dosłownie dom pod osikami i pojawia się w polskich źródłach historycznych również jako `` Ossowa Sień '' lub `` Ossowasień '' .
Niemiecka nazwa pochodzi od nazwiska niemieckiego osadnika Rydygiera i występuje w starszych wariantach jako `` Rüdigersdorf '' ( jak starsza nazwa miejscowości Rudgorzewice ) lub w nowszej wersji skróconej `` Röhrsdorf '' ( podobnie jak starsza nazwa Rochowic ) .
