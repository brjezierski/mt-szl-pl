Ostenda
Ostenda ( , ) – miasto w północno-zachodniej Belgii , na wybrzeżu Morza Północnego , w prowincji Flandria Zachodnia .
Historia .
Początki i średniowiecze .
Ostenda była na początku małą wioską położoną na wschodnim końcu ( flamandzki : oost-einde ) wyspy zwanej Testerep .
Choć mała , wioska zyskała status miasta około roku 1265 .
Głównym przychodem mieszkańców było rybołówstwo .
Wybrzeże Morza Północnego było bardzo niestabilne i w roku 1395 mieszkańcy zdecydowali się zbudować nową Ostendę za wielką groblą i dalej od morza .
XV–XVIII wiek .
Strategiczne położenie było zaletą miasta , ale również przyczyną jego kłopotów .
Miasto było często zdobywane i niszczone .
Najważniejszym takim zdarzeniem było trzyletnie oblężenie Ostendy w latach 1601–1604 .
