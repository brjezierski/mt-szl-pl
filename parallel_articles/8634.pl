Osterholz-Scharmbeck
Osterholz-Scharmbeck ( dolnoniem .
`` Oosterholt-Scharmbeck '' ) − miasto powiatowe w Niemczech , w kraju związkowym Dolna Saksonia , siedziba powiatu Osterholz .
Geografia .
Osterholz-Scharmbeck położony jest ok. 22 km na północ od Bremy .
Przez obszar miasta przebiega droga krajowa B74 .
