Osłonino
Osłonino ( kaszb .
`` Òsłónino '' , niem .
`` Oslanin '' , dawniej `` Oslonino '' ) – wieś w Polsce położona w województwie pomorskim , w powiecie puckim , w gminie Puck , nad Zatoką Pucką , u ujścia rzeki Gizdepki , na Kaszubach .
Miejscowość z przystanią morską dla rybaków ( pas plaży ) .
Wieś powstała w 1355 roku .
Należała do dóbr rzucewskich i rzucewsko-wejherowskiej .
Wieś klasztoru kartuzów w Kartuzach w powiecie puckim województwa pomorskiego w II połowie XVI wieku .
Podczas zaboru pruskiego wieś nosiła nazwę niemiecką `` Oslanin '' .
Podczas okupacji niemieckiej nazwa `` Oslanin '' w 1942 została przez nazistowskich propagandystów niemieckich ( w ramach szerokiej akcji `` odkaszubiania i odpolszczania nazw niemieckiego lebensraumu '' ) zweryfikowana jako zbyt `` kaszubska '' i przemianowana na `` nowo wymyśloną i bardziej niemiecką '' – `` Truchsassen '' .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Na terenie wsi wykryto bardzo starą osadę garncarzy , której istnienie potwierdzają wydobyte z ziemi wyroby rzemieślnicze oraz wczesne groby skrzynkowe .
W okolicach Osłonina znajduje się rezerwat przyrody `` Beka '' , w którym można spotkać wiele unikatowych ptaków na skalę europejską i światową , a także wznoszący się na wysokość 16 m Klif Osłoniński .
We wsi znajduje się niewielkie molo spacerowe .
W Osłoninie w roku 1935 urodził się Hilmar Kopper prezes zarządu Deutsche Bank ( 1989–1997 ) .
