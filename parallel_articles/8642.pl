Ottignies-Louvain-la-Neuve
Ottignies-Louvain-la-Neuve – miasto w Belgii , w prowincji Brabancja Walońska .
Mieści się tu otwarte w czerwcu 2009 roku muzeum poświęcone pracy ilustratora Georges ’ a Remi znanego jago Herge , autora komiksu Tintin .
Dwa najważniejsze miasta aglomeracji to :
Park naukowy Louvain-la-Neuve .
Stworzony w roku 1971 Park Naukowy Louvain-la-Neuve jest pierwszym tego rodzaju w Belgii i największym w Walonii ( francuskojęzycznej części Belgii ) .
Park zajmuje obszar 231 hektarów na terenie aglomeracji Ottignies-Louvain-la-Neuve i Mont-Saint-Guibert ( 30 km od Burkseli ) .
Głównym celem stworzenia i rozwoju parku był rozwój kooperacji pomiędzy przemysłem i Katolickim Uniwersytetem Lowańskim i pobudzenie ekonomiczne regionu .
Szczególny nacisk położony jest na ekologiczność .
W Parku naukowym Louvain-la-Neuve Science Park ma swoją siedzibę ponad 130 firm , które zatrudniają 4500 pracowników i 3 centra biznesowe .
