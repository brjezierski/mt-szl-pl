Otton I Wielki
Otton I Wielki ( ur .
23 listopada 912 , zm .
7 maja 973 w Memleben ) – książę Saksonii 936–961 , król niemiecki od 936 i cesarz rzymski od 962 , z dynastii Ludolfingów .
Życiorys .
Był synem Henryka I Ptasznika , króla Niemiec i jego drugiej żony Matyldy z Ringelheim , córki saskiego hrabiego Dytryka .
Otton został królem Niemiec w drodze elekcji w roku 936 .
Uroczysta koronacja odbyła się w Akwizgranie w tym samym roku .
Po koronacji musiał zwalczyć bunt książąt Saksonii , Bawarii , Lotaryngii i Frankonii wspieranych przez braci Ottona Thankmara i Henryka .
Po zwycięstwie w 939 roku rozpoczął proces umacniania władzy opierając się na Kościele i związkach dynastycznych .
Prowadził politykę ekspansywną wobec Słowian tworząc na ich terenach liczne marchie , osiągając brzegi Odry .
Uzależnił od Niemiec Włochy , Czechy i Danię .
Podbitą ludność chrystianizował .
10 sierpnia 955 pokonał Madziarów w bitwie na Lechowym Polu koło Augsburga , powstrzymując ich najazdy na środkową Europę .
Od 963 roku Otton otrzymywał trybut od Mieszka I , księcia Polan ( według przekazu kronikarza Thietmara ) z części jego ziem – ta informacja bywa jednak kwestionowana przez znaczną część badaczy .
2 lutego 962 papież Jan XII koronował Ottona w Rzymie na cesarza rzymskiego .
Chcąc jednak ułatwić sobie uznanie ze strony Bizancjum używał on tego tytułu bez przymiotnika „ rzymski ” .
Jeszcze za swego życia w 967 Otton I uczynił cesarzem swego syna Ottona II .
Po roku 967 cesarz Zachodu Otton I Wielki zamierzał doprowadzić do pojednania z Cesarstwem Bizantyńskim .
Domagał się jednak prowincji bizantyńskich na Półwyspie Apenińskim .
W tym celu Otton wysłał do Konstantynopola biskupa Cremony Liutpranda .
Misja zakończyła się fiaskiem , a Bizancjum wysłało do Italii wojsko i okręty wojenne .
Otton I podjął wówczas najazd na ziemie Cesarstwa Bizantyńskiego w Italii .
Wiosną roku 969 przerwał wyprawę i opuścił południowe Włochy pozostawiając dalszą wojnę Pandolfowi z Kapui , który wkrótce dostał się do niewoli bizantyńskiej .
W tej sytuacji Otton posłał na południe Włoch kolejne wojska , które rozbiły armię bizantyńską w bitwie pod Ascoli ( Asculum ) w roku 969 .
Mimo zwycięstwa Ottona sytuacja w południowych Włoszech nie uległa zmianie .
W zaistniałej sytuacji w roku 972 Otton zaproponował Cesarstwu Bizantyjskiemu zwrot podbitych ziem na terenie Włoch w zamian za wzajemne uznanie tytułów cesarskich .
Układ ten został przypieczętowany małżeństwem Ottona II z księżniczką Teofano , krewną cesarza bizantyjskiego Jana I Tzimiskesa .
Otton I zmarł w wieku 60 lat i został pochowany w katedrze w Magdeburgu .
Małżeństwa i potomstwo .
Otton I był dwukrotnie żonaty .
Pierwszą żoną była Edyta Angielska , córka króla Edwarda Starszego .
Po raz drugi ożenił się w październiku 951 roku z Adelajdą , córką króla Burgundii – Rudolfa II .
