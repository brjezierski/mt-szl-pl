Otto Stern
Otto Stern ( ur .
17 lutego 1888 w Żorach , zm .
17 sierpnia 1969 w Berkeley w stanie Kalifornia ) – niemiecki fizyk , noblista .
Życiorys .
Żory i Wrocław ( 1888–1912 ) .
Był wnukiem bogatego kupca żydowskiego , właściciela żorskiego młyna , Abrahama Sterna i synem Oskara Sterna i Eugenii ( z domu Rosenthal ) .
Miał czworo młodszego rodzeństwa ( Berta , Kurt , Lota i Liza ) .
W roku 1892 Sternowie przeprowadzili się do Wrocławia .
Po ukończeniu szkoły podstawowej Otto Stern kontynuował naukę w `` Johannes-Gymnasium '' we Wrocławiu , a następnie podjął studia uniwersyteckie we Fryburgu Bryzgowijskim , Monachium i Wrocławiu .
Stopień doktora chemii fizycznej uzyskał na Uniwersytecie Wrocławskim w roku 1912 .
Jego rozprawa doktorska dotyczyła ciśnienia osmotycznego dwutlenku węgla w stężonych roztworach .
Miała charakter zarówno praktyczny , jak też i teoretyczny .
W przyszłości ustaliła jego działalność badawczą , dzięki której sam siebie określił jako „ teoretyka doświadczalnego ” .
Praga i Zurych ( 1912–1913 ) , Niemcy ( 1914–1933 ) .
Po doktoracie , w tymże roku , przeniósł się – za Albertem Einsteinem – na Uniwersytet Karola w Pradze , a w następnym roku – na Politechnikę w Zurychu ( ETH ) .
W Szwajcarii zawarł znajomość z takimi wybitnymi fizykami , jak Paul Ehrenfest i Max von Laue .
W tym czasie jego finansowa niezależność pozwoliła mu obrać miejsce pracy bez oglądania się na osiąganie większych korzyści materialnych .
W roku 1913 otrzymał w Wyższej Szkole Technicznej w Zurychu `` venia legendi '' , czyli prawo prowadzenia własnych odczytów .
Stał się tym samym tzw .
`` Privatdozent '' , co uprawniało go do prowadzenia wykładów na uniwersytetach jednak bez prawa do wynagrodzenia za przeprowadzane wykłady .
