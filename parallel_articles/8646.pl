Otylia Jędrzejczak
Otylia Jędrzejczak ( ur .
13 grudnia 1983 w Rudzie Śląskiej ) – polska pływaczka specjalizująca się w stylu motylkowym i dowolnym , mistrzyni olimpijska , dwukrotna mistrzyni świata , pięciokrotna mistrzyni Europy na długim basenie , trzykrotna rekordzistka świata .
Od 2021 prezes Polskiego Związku Pływackiego .
W latach 2004–2006 wybierana najlepszym polskim sportowcem w plebiscycie „ Przeglądu Sportowego ” .
W 2005 okrzyknięta najlepszą pływaczką w Europie przez magazyn „ Swimming World ” .
W 2019 wprowadzona do międzynarodowej galerii sław pływania International Swimming Hall of Fame .
Życiorys .
Jest córką Krystyny i Piotra Jędrzejczaków .
Jej ojciec był pięściarzem i pracował jako górnik .
Wychowała się w Kochłowicach , gdzie uczęszczała do Szkoły Podstawowej nr 19 .
W dzieciństwie trenowała szermierkę .
Z czasem przeprowadziła się z rodziną do Katowic , gdzie ukończyła Szkołę Podstawową nr 30 w klasie o profilu pływackim .
W wieku sześciu lat zaczęła uczęszczać na regularne zajęcia pływackie , co zalecił jej lekarz , diagnozując u niej skrzywienie kręgosłupa .
Jako podopieczna Pałacu Młodzieży w Katowicach w 1994 zajęła pierwsze miejsce na mistrzostwach Polski w Radlinie .
Jej ówczesnymi trenerami byli , kolejno : Marian Syposz , Jacek Czarnecki , Jan Barucha i Marek Jabczyk .
Po ukończeniu nauki w szkole podstawowej trafiła do Szkoły Mistrzostwa Sportowego w Krakowie , gdzie jej trenerką była Maria Jakóbik .
W tym czasie dostała się do narodowej kadry juniorskiej , a jej trenerem był Edmund Uścinowicz .
W 1998 została finalistką Światowych Igrzysk Młodzieży w Moskwie .
W 1999 na mistrzostwach Europy w Stambule zdobyła brązowy medal w konkurencji 200 m stylem motylkowym , a rok później została mistrzynią Europy w tej konkurencji , uzyskawszy czas 2:08,63 , a także zdobyła srebrny medal na 100 m stylem motylkowym .
Trzy miesiące później podczas igrzysk olimpijskich w Sydney w półfinale 200 m stylem motylkowym ustanowiła rekord Europy ( 2:07,81 ) .
W finale tej konkurencji była piąta z czasem 2:08,48 , z kolei na dystansie 100 m stylem motylkowym uzyskała wynik 59,16 s i zajęła dziewiąte miejsce .
Po trzech latach trenowania w Szkole Mistrzostwa Sportowego odeszła z klubu i wyjechała do Hanoweru , gdzie rozpoczęła treningi pod okiem Petera Fischera .
W 2001 na mistrzostwach świata w Fukuoki zdobyła srebrny medal na dystansie 100 m stylem motylkowym .
Powróciwszy na stałe do Polski , dołączyła do klubu AZS-AWF Warszawa , a jej trenerem został Paweł Słomiński .
4 sierpnia 2002 na mistrzostwach Europy w Berlinie czasem 2:05,78 pobiła rekord świata na 200 m stylem motylkowym .
Zajęła drugie miejsce w plebiscycie „ Przeglądu Sportowego ” na sportowca roku .
