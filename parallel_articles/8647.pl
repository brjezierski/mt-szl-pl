Wagadugu
Wagadugu ( fr .
`` Ouagadougou '' ) – miasto w środkowej Burkinie Faso , stolica i największe miasto tego kraju , ośrodek administracyjny prowincji Kadiogo .
W 2019 roku zamieszkiwane przez blisko 2,5 mln mieszkańców .
Historia .
Miasto założone zostało w XV wieku .
W 1896 zostało zajęte przez Francuzów .
Od 1919 roku było siedzibą władz kolonialnych , a od 1960 stolicą Górnej Wolty .
Po zmianie nazwy kraju na Burkina Faso Wagadugu pozostało stolicą .
