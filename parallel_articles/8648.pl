Oudenaarde
Oudenaarde ( franc .
`` Audenarde '' ) – miasto w Belgii położone nad Skaldą w prowincji Flandria Wschodnia , na południe od Gandawy , zwane jest perłą flamandzkich Ardenów .
Patronką miasta jest św .
Walburga .
Od XV do XVIII wieku ( szczególnie w XVI ) miasto słynęło z wyrobu gobelinów .
Oudenaarde jest gminą , która łączy w sobie szereg mniejszych miejscowości : Bevere , Edelare , Eine , Ename , Heurne , Leupegem , Mater , Melden , Mullem , Nederename , Volkegem i Welden .
Historia .
Miasto powstało ok. XI wieku w dolinie rzeki Skaldy , co stwarzało doskonałe możliwości rozwoju .
