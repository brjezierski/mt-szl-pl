Oulu ( prowincja )
Oulu była jedną z 6 prowincji Finlandii .
Graniczyła z prowincjami Laponia , Finlandia Zachodnia i Finlandia Wschodnia .
Stolicą prowincji było Oulu .
Zajmowała teren historycznego regionu Österbotten .
Historia .
Prowincja Oulu powstała w 1775 .
Od początku XIX w. aż do 1936 do prowincji należała część Laponii .
Została zniesiona 1 stycznia 2010 r .
Regiony .
Prowincja Oulu była podzielona na 2 regiony .
