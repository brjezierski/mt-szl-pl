Ourense ( prowincja )
Ourense ( ) – prowincja Hiszpanii , w regionie Galicja .
Stolicą jest Ourense .
Prowincja jest położona w południowo - wschodniej części hiszpańskiej Galicji , nad Oceanem Atlantyckim i graniczy ona bezpośrednio z Portugalią .
Około 32 % ludności mieszka w samej stolicy .
Łącznie prowincję stanowią 92 gminy .
Płyną tędy dwie rzeki Miño i Sil mające swe ujście do Oceanu Atlantyckiego .
Region , podobnie jak całą Galicja znajduje się pod wpływem klimatu oceanicznego , charakteryzującego się łagodnymi temperaturami przez cały rok .
