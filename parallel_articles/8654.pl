Ousmane Dembélé
Ousmane Dembélé ( ur .
15 maja 1997 w Vernon ) – francuski piłkarz malijskiego pochodzenia , występujący na pozycji napastnika w hiszpańskim klubie FC Barcelona oraz w reprezentacji Francji .
Złoty medalista Mistrzostw Świata 2018 i srebrny medalista Mistrzostw Świata 2022 .
Kariera klubowa .
Dembélé jest wychowankiem Stade Rennais .
W maju 2016 został piłkarzem Borussii Dortmund .
Po udanym sezonie 2016/2017 Dembélé zaczął wzbudzać zainteresowanie innych klubów .
Ostatecznie w sierpniu 2017 podpisał pięcioletni kontrakt z FC Barceloną .
W nowym klubie zadebiutował 9 września , zmieniając Gerarda Deulofeu w wygranym 5:0 ligowym meczu z RCD Espanyol , w którym zanotował asystę przy golu Luisa Suáreza .
Pierwszą bramkę dla Barcelony strzelił 14 marca 2018 w wygranym 3:0 meczu Ligi Mistrzów UEFA z Chelsea .
17 kwietnia zanotował pierwsze ligowe trafienie , trafiając w meczu z Celtą Vigo ( 2:2 ) .
Kariera reprezentacyjna .
W reprezentacji Francji zadebiutował 1 września 2016 w wygranym 3:1 meczu przeciwko reprezentacji Włoch .
Pierwszą bramkę dla reprezentacji strzelił 13 czerwca 2017 w wygranym 3:2 towarzyskim meczu przeciwko reprezentacji Anglii .
Jest mistrzem świata z 2018 .
