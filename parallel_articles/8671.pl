Ołysziwka
Ołysziwka ( ) – osiedle typu miejskiego na Ukrainie , w obwodzie czernihowskim , w rejonie czernihowskim , siedziba hromady .
W 2001 liczyło 2389 mieszkańców , spośród których 2261 wskazało jako ojczysty język ukraiński , 112 rosyjski , 2 mołdawski , 1 bułgarski , 9 białoruski , 1 ormiański , 1 polski , a 2 inny .
