Pabianice ( gmina wiejska )
Pabianice ( do 1954 `` gmina Górka Pabianicka '' ) – gmina wiejska w województwie łódzkim , w powiecie pabianickim .
W latach 1975–1998 gmina położona była w ówczesnym województwie łódzkim .
Siedziba Urzędu Gminy nie znajduje się na jej terenie , lecz w sąsiadującym z gminą mieście Pabianice .
W skład gminy wchodzi 18 sołectw .
Według danych z listopada 2014 gminę zamieszkiwało 6368 osób .
Natomiast według danych z 31 grudnia 2017 roku gminę zamieszkiwało 7238 osób .
Położenie .
Położenie geograficzne gminy jest nietypowe : dwie części leżą na północy i południu od Pabianic , przedzielone powierzchnią miasta .
Teren leży na wysokości 163 – 200 m n.p.m .
