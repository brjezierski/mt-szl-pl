Podbrodzie ( stacja kolejowa )
Podbrodzie ( , ) – stacja kolejowa w miejscowości Podbrodzie , w rejonie święciańskim , na Litwie .
Leży na linii dawnej Kolei Warszawsko-Petersburskiej .
Stacja powstała w XIX w. pomiędzy przystankiem Drużyły a stacją Święciany .
Dawniej był to węzeł kolejowy .
Rozpoczynała się tu linia do stacji Królewszczyzna , linii Połock – Mołodeczno .
Obecnie jej fragment od Podbrodzia do granicy litewsko-białoruskiej jest rozebrany .
