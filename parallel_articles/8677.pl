Pacanowice ( przystanek kolejowy )
Pacanowice – dawny przystanek osobowy w Pacanowicach , w gminie Pleszew , w powiecie pleszewskim , w woj .
wielkopolskim , w Polsce .
Położony na dawnej wąskotorowej linii kolejowej z Krotoszyna Wąskotorowego do Broniszewic .
Został wybudowany w czasie II wojny światowej przez Niemców .
W latach 70 .
XX wieku został rozebrany .
