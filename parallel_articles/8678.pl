Pacanów ( województwo śląskie )
Pacanów – wieś w Polsce położona w województwie śląskim , w powiecie kłobuckim , w gminie Panki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa częstochowskiego .
We wsi znajduje się przystanek autobusowy , z którego odjeżdżają autobusy m.in .
do Kłobucka i Krzepic .
