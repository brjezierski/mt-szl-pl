Paczków ( gromada )
Paczków ( pocz .
`` Paczków Wieś '' ) – dawna gromada , czyli najmniejsza jednostka podziału terytorialnego Polskiej Rzeczypospolitej Ludowej w latach 1954–1972 .
Gromady , z gromadzkimi radami narodowymi ( GRN ) jako organami władzy najniższego stopnia na wsi , funkcjonowały od reformy reorganizującej administrację wiejską przeprowadzonej jesienią 1954 do momentu ich zniesienia z dniem 1 stycznia 1973 , tym samym wypierając organizację gminną w latach 1954–1972 .
Gromadę Paczków Wieś z siedzibą GRN w mieście Paczkowie ( nie wchodzącym w skład gromady ) utworzono 31 grudnia 1959 w powiecie nyskim w woj .
opolskim z obszarów zniesionych gromad Kamienica i Unikowice w tymże powiecie .
Dla gromady ustalono 27 członków gromadzkiej rady narodowej .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 w powiecie nyskim utworzono gminę Paczków .
