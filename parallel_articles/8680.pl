Paczyn
Paczyn ; niem .
`` Paczoldisdorf '' ( 1325–1840 ) ; `` Petzelsdorf '' ( 1840–1945 ) – wieś w Polsce położona w województwie dolnośląskim , w powiecie kamiennogórskim , w gminie Lubawka .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa jeleniogórskiego .
Demografia .
Jest najmniejszą miejscowością gminy Lubawka .
Według Narodowego Spisu Powszechnego posiadał 69 mieszkańców ( III 2011 r. ) .
Historia .
Niewielka wioska , która do połowy XVIII w. znajdowała się na terenie Księstwa Świdnickiego .
Najprawdopodobniej wieś powstała w 2 połowie XIV w. i stanowiła własność rycerską .
Zawsze miała bardzo dużo wspólnego z Miszkowicami , a od 1620 roku należały do hrabstwa Von Schaffgotschów .
Na zachodnim krańcu miejscowości odbywa się festiwal muzyki elektronicznej LAS Festival .
