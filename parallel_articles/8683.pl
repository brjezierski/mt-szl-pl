Paducah ( Kentucky )
Paducah – miasto ( `` city '' ) , ośrodek administracyjny hrabstwa McCracken , w zachodniej części stanu Kentucky , w Stanach Zjednoczonych , położone nad ujściem rzeki Tennessee do rzeki Ohio .
W 2012 roku miasto liczyło 25 048 mieszkańców .
Początek osadnictwa na tym obszarze , zwanym początkowo Pekin , nastąpił około 1815 roku .
Osadę zamieszkiwała wówczas mieszana społeczność pochodzenia indiańskiego i europejskiego .
W 1827 roku ziemia ta stała się własnością Williama Clarka , który nadał miejscowości obecną nazwę , upamiętniającą Paduke ' a , legendarnego indiańskiego wodza z plemienia Czikasawów .
