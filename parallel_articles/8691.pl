Historia .
Utworzenie kraju .
Pakistan odłączył się od Indii Brytyjskich w 1947 .
Gdy Brytyjczycy zorganizowali na przełomie 1945 i 1946 wybory do władz ustawodawczych , Liga Muzułmańska zdobyła większość w rządach Bengalu i Sindhu , zaś w Pendżabie utworzyła rząd koalicyjny .
Tym samym roszczenia muzułmanów do stworzenia państwa islamskiego stały się uzasadnione .
W 1947 ostatni wicekról Indii , lord Louis Mountbatten , opracował plan , na podstawie którego kolonia brytyjska podzielona została na dwa suwerenne państwa : Indie i Pakistan .
14 sierpnia 1947 Pakistan uzyskał niepodległość ( do 1956 jako dominium brytyjskie z generalnym gubernatorem reprezentującym brytyjskiego monarchę ; w 1956 proklamowano republikę ) .
Jesienią 1947 wybuchała rewolta o podłożu religijnym , do Kaszmiru wkroczyły pathańskie plemiona z Pakistanu , więc maharadża Singh zmuszony był poprosić o pomoc Indie .
26 października podpisał on akt wstąpienia Kaszmiru do Indii .
Pakistan nie uznał faktu przyłączenia się Kaszmiru do Indii .
Muhammad Ali Jinnah , przywódca Pakistanu zwrócił się z prośbą do lorda Mountbattena o mediację .
Louis Mountbatten poprosił Singha o przeprowadzenie plebiscytu wśród ludności , jednak maharadża tę prośbę odrzucił .
W ten sposób zaczął się konflikt indyjsko-pakistański , trwający do dziś .
W 1954 roku odbyły się wybory , które pokazały duże rozbieżności między wschodnią a zachodnią częścią kraju .
Na zachodzie wyborcze zwycięstwo odniosła proamerykańska Partia Republikańska natomiast na wschodzie ugrupowanie lewicowe tj .
Pakistańska Partia Komunistyczna , Liga Awami czy Partia Robotnicza .
W rezultacie rządy we wschodnim Pakistanie objęła radykalna lewica .
Sprawujący władzę w drugiej połowie lat 50. premier Huseyn Shaheed Suhrawardy i jego lewicujący rząd ustanowił prozachodni i proamerykański kurs kontynuowany przez późniejsze rządy .
W 1958 roku w wyniku bezkrwawego zamachu stanu władzę autorytarną przejął wojskowy – Muhammad Ayub Khan , który postawił na dobre relacje z USA .
Dzięki ogromnej pomocy finansowej rządu amerykańskiego Pakistan przeżywał kilkuletni okres ożywienia gospodarczego i stabilizacji politycznej .
Równocześnie zawarto przyjazne stosunki z krajami Europy m.in .
Polską .
Ministrem spraw zagranicznych jego rządu został Zulfikar Ali Bhutto .
W 1966 stolica została przeniesiona z Karaczi do Islamabadu .
Przegrana w II wojnie o Kaszmir przyczyniła się do gwałtownego spadku poparcia społecznego dla Ayub Khana .
Pogorszył się również stan jego zdrowia .
Ulegając namowom wojskowych , w 1969 ustąpił na rzecz generała Yahyi Khana .
W 1970 wycofał się z życia politycznego .
Po konfliktach z przywódcami kraju , Bhutto opuścił rząd i stanął na czele opozycji .
26 marca 1971 Pakistan Wschodni odłączył się tworząc niepodległy Bangladesz .
Doszło do tego po tym , gdy Yahya Khan , dyktator kraju , zarządził akcję wojskową pod kryptonimem `` Searchlight '' skierowaną przeciwko bengalskim separatystom .
Akcja nie udała się , a w wyniku kompromitacji rządu nowym premierem został Bhutto .
W latach 1971–1977 premierem Pakistanu był Zulfikar Ali Bhutto .
