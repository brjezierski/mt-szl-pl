Palermo FC
Palermo Football Club – włoski klub piłkarski założony 1 listopada 1900 z siedzibą w mieście Palermo na Sycylii .
Obecnie występuje w Serie B .
Historia .
Chronologia nazw :
Założony został w roku 1900 jako Anglo Panormitan Athletic and Football Club , w 1907 zmienił nazwę na Palermo FBC i przybrał różowo-czarne barwy .
W sezonie 2004/2005 był jednym z czołowych zespołów Serie A , zajął 6. miejsce w ligowej tabeli i zakwalifikował się do Pucharu UEFA , w którym zajął 1. miejsce w swojej grupie i ostatecznie dotarł do 1/8 finału .
W sezonie 2022/23 drużyna występuje w rozgrywkach Serie B. Palermo to trzykrotny finalista Pucharu Włoch .
