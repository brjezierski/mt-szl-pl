Palm Beach
Palm Beach – miasto w Stanach Zjednoczonych , położone w hrabstwie Palm Beach w stanie Floryda .
Od sąsiednich miejscowości West Palm Beach i Lake Worth oddziela je kanał Intracoastal Waterway .
Według danych U.S. Census Bureau za rok 2004 Palm Beach ma 9860 stałych mieszkańców , przy czym sezonowo liczba ta wzrasta do 30 tys .
Historia .
Swoją nazwę wyspa ( i miasto ) zawdzięcza katastrofie hiszpańskiego żaglowca `` Providencia '' w 1878 roku .
