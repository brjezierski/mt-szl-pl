Palma ( Hiszpania )
Palma ( w różnych okresach przed 2 grudnia 2016 : Palma de Mallorca ) – stolica i największe miasto Balearów oraz wyspy Majorka .
Jest jednym z największych ośrodków turystycznych świata , a jej port lotniczy jednym z najbardziej ruchliwych w Europie .
Funkcjonuje w nim jedna linia metra .
W Palmie znajduje się również jeden z najbardziej popularnych portów dla wielkogabarytowych statków wycieczkowych .
Ma liczne walory turystyczne , w tym katedrę ( budowaną w latach 1230−1600 ) .
Inna zabytkowa budowla , pałac Almudaina ( katal .
`` palau de l ’ Almudaina '' ) , jest połączeniem stylów aragońskich i arabskich .
Palma jest również znana z życia nocnego i olbrzymiej liczby klubów , barów , restauracji i dyskotek .
