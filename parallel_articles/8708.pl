Palmanova ( stacja kolejowa )
Palmanova ( włoski : `` Stazione di Palmanova '' ) – stacja kolejowa w Palmanovie , w regionie Friuli-Wenecja Julijska , we Włoszech .
Znajdują się tu 2 perony .
Historia .
Stacja została otwarta 26 sierpnia 1888 roku , kiedy otworzyli linię kolejową do Udine i San Giorgio di Nogaro .
Dnia 1 stycznia 1917 roku stacja zyskała połączenie kolejowe z Cervignano .
W 1997 linia kolejowa do San Giorgio di Nogaro została zamknięta .
