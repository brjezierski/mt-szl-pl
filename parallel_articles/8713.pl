Palmira ( miasto w Kolumbii )
Palmira — miasto w zachodniej Kolumbii , w departamencie Valle del Cauca .
Położone jest w Kordylierze Środkowej , w dolinie rzeki Cauca , na wysokości 1040 m n.p.m .
