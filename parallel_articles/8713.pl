Miasto wchodzi w skład aglomeracji Cali .
Miasto zostało założone około 1794 .
Palmira stanowi ważny ośrodek handlu dla otaczającego go rolniczego regionu uprawy tytoniu , kawy , kakao i trzciny cukrowej .
Rozwinął się tutaj głównie przemysł cukrowniczy , mięsny oraz włókienniczy .
Palmira położona jest przy Autostradzie Panamerykańskiej .
Miasto stanowi również węzeł kolejowy , z którego odchodzą pociągi na zachód do Buenaventuru , na południe do Cali i Popayan oraz na północ do Medellin i dalej do północnej Kolumbii .
W mieście znajduje się główny port lotniczy obsługujący Cali .
W mieście znajduje się filia uniwersytetu `` Universidad Pontificia Bolivariana '' .
