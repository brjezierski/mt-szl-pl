Palo Alto ( Aguascalientes )
Palo Alto – miasto w środkowym Meksyku , w północno-wschodniej części stanu Aguascalientes , siedziba władz gminy El Llano .
Miejscowość jest położona na płaskowyżu , w górach Sierra Madre Wschodnia na wysokości 2 027 m n.p.m .. Palo Alto leży około 40 km na wschód od stolicy stanu Aguascalientes .
W 2010 roku ludność miasta liczyła 5 399 mieszkańców .
Miasto powstało z hacjendy należącej do Rincon Gallardo , gubernatora Aguascalientes w latach 70 .
XIX wieku .
Tworzy miasto bliźniacze z El Llano .
Dekretem gubernatora Agusacalientes w 1992 zostało ustanowione siedzibą gminy El Llano .
W mieście warto zwiedzić zabytkowe budynki hacjendy oraz forty których najstarsza część sięga 1873 roku , a także budynki `` Las Trojes '' i stajnie oraz audytorium Palo Alto .
Ponadto jest bardzo ładnie prezentuje się neoklasycystyczny kościół z 1921 roku .
