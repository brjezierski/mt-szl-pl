Pampeluna ( turniej szachowy )
Pampeluna ( hiszp .
Ciudad de Pamplona ) – jeden z najsilniej obsadzonych szachowych turniejów organizowanych w Hiszpanii , rozgrywany pod koniec roku kalendarzowego ( do 2002 r. rozgrywany na przełomie lat ) w Pampelunie .
Pierwszy turniej odbył się w roku 1990 i z wyjątkiem przełomu lat 2000/2001 organizowany jest cyklicznie .
Wszystkie zawody rozegrano systemem kołowym w obsadzie od 8 do 10 zawodników ( z wyjątkiem edycji 2002/2003 , kiedy rozegrano dwa koła w obsadzie 4-osobowej ) .
W dotychczasowej historii zwycięzcami byli m.in .
mistrzowie świata Rusłan Ponomariow i Rustam Kasimdżanow .
Jedynym polskim szachistą , który uczestniczył w turnieju był w roku 1998 Michał Krasenkow ( zajmując II miejsce ) .
