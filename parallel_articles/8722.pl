Panaji
Panaji ( hindi पणजी , trb .
: `` Panadźi '' , trl .
: `` Paṇajī '' ; ang .
`` Panaji '' , dawniej `` Panjim '' ) – miasto w Indiach , stolica stanu Goa .
Położone nad estuarium rzeki Mandovi niedaleko od Morza Arabskiego .
Zamieszkane jest przez 65 000 osób ( trzecie co do wielkości miasto stanu ) .
Miasto zostało założone na miejscu małej wioski Pangem i nazwane w 1843 `` Nova Goa '' po tym jak stało się stolicą Indii Portugalskich .
Po przyłączeniu Goa do Indii od 1961 było stolicą związkowego terytorium Goa , Daman i Diu , a od 1987 stanu Goa .
