Pancrace Bessa
Pancrace Bessa ( ur .
1772 , zm .
1835 ) – malarz , grafik i rysownik francuski , szczególnie znany z ilustracji botanicznych .
Był uczniem holenderskiego malarza i ilustratora Gerarda van Spaendoncka .
Malował głównie owoce i kwiaty , okazjonalnie ptaki i ssaki , posługiwał się najchętniej akwarelą .
W 1816 Karolina Burbon-Sycylijska synowa króla Francji Karola X Burbona objęła artystę patronatem .
Malarz był nauczycielem sztuki rodziny książęcej i wykonywał na jej zamówienie szereg prac m.in .
zbiór akwarel `` Velins du Roi '' .
Najważniejszym współpracownikiem Bessy był belgijski malarz i botanik Pierre-Joseph Redouté , z którym wspólnie wykonali ilustracje do kilku fundamentalnych dzieł z zakresu botaniki .
Były to `` Histoire des Arbres Forestiers de L ’ Amerique Septentrionale '' wydana w latach 1810–1813 , `` L ’ Herbier Général de L ’ Amateur '' ( 1810–1826 ) , `` Description des Plantes cultivees a Malmaison a Navarre '' oraz wydana po śmierci malarza `` Flore des Jardiniers '' ( 1836 ) .
