Pandemia
Pandemia ( gr .
`` pan '' „ wszyscy ” , `` demos '' „ lud ” ) – nazwa epidemii o szczególnie dużych rozmiarach , na dużym obszarze , obejmującej kraje , a nawet kontynenty .
Pandemie a globalizacja .
Według niektórych opinii przyczyną pandemii może być globalizacja , która znacznie ułatwia rozprzestrzenianie się chorób .
Rządy państw starają się przeciwstawiać rozprzestrzenianiu się pandemii , ale są to z reguły działania doraźne , często niewystarczające .
Wiąże się to np .
z nakładami finansowymi , jak i z niemożliwością zahamowania migracji ludności .
Jednocześnie globalizacja oznacza poprawę warunków bytowych ludzi i zmniejszenie ryzyka wystąpienia pandemii , w związku z podnoszeniem się jakości i dostępności opieki medycznej .
