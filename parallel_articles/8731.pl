Pano Lefkara
Pano Lefkara ( gr .
`` Πάνω Λεύκαρα '' ) – miasto w Republice Cypryjskiej , w dystrykcie Larnaka .
W 2011 roku liczyło 762 mieszkańców .
Leży na południowo-wschodnim zboczu gór Trodos .
Miasto znane jest z tradycji koronkarskich , które w 2009 roku zostały wpisane na listę niematerialnego dziedzictwa UNESCO .
Nazwa wioski Lefkara pochodzi od koloru otaczających ją białych wapiennych skał .
Kościół .
Kościół Świętego Krzyża pochodzi z XIV wieku .
Potwierdza to manuskrypt Ewangelii z XIV wieku przechowywany w kościele .
W 1740 roku kościół został odrestaurowany i umieszczono w nim rzeźbiony w drewnie ikonostas wykonany przez rzeźbiarza Hadjikyriacosa .
Jest w nim przechowywany drewniany krzyż , którego wezwanie nosi kościół .
