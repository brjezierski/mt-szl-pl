Panteizm
Panteizm ( z gr .
`` pan '' „ wszystko ” , `` theos '' „ Bóg ” ) – pogląd filozoficzno-religijny ( niekiedy teologiczny ) utożsamiający wszystko z bogiem ( lub absolutem ) .
Neguje istnienie boga osobowego , głosi zaś przenikanie absolutu we wszystkie substancje jakie istnieją .
Panteizm często łączył się z ideami rozumnego rozwoju wszechświata , jedności , wieczności oraz żywości świata materialnego .
Do ważniejszych filozofii panteistycznych można zaliczyć stoicyzm , neoplatonizm , teozofię , filozofię Jana Szkota Eriugeny , Giordana Bruna oraz Barucha Spinozy .
Idee panteizmu głosiło też wiele ruchów religijnych : m.in .
średniowieczni amalrycjanie i begardzi .
Koncepcja ta nie ma natomiast nic wspólnego z ideą przebóstwienia ( `` theopoiesis '' ) wyznawaną w chrześcijaństwie , zwłaszcza w prawosławiu , która głosi możliwość powrotu dusz do Boga i połączenia z Bogiem .
Historia .
Termin panteizm pochodzi od greckiego słowa `` pan '' ( gr .
πᾶν ) oznaczającego „ wszystko ” oraz `` theos '' ( θεός ) oznaczającego boga , rozumianego szerzej jako istotę teizmu .
