Papier
Papier ( z gr .
πάπυρος ( pápyros ) , łac .
`` carta papirea '' ) – spilśniona na sicie masa włóknista pochodzenia organicznego o gramaturze od 28 do 200 g/m² .
Wytwarzany poprzez ułożenie na sicie włókien .
Papier jest wytwarzany w formie arkuszy lub wstęgi nawijanej w zwoje .
Po uformowaniu masy na sicie jest odwadniany , prasowany , suszony i gładzony w podzielonych etapach ciągłego procesu wytwarzania .
Używane są zwykle włókna organiczne : z celulozy , włókno ścieru drzewnego – otrzymywane poprzez starcie i zmielenie bali sosnowych ( tzw .
`` papierówki '' ) w procesie rozwłókniania mechanicznego .
Czasem stosowany jest proces rozwłókniania chemicznego i mają zastosowanie inne włókna roślinne ( słoma , trzcina , bawełna , len , konopie , bambus ) .
Zastosowanie ma też makulatura uprzednio poddana procesowi dyspersji .
Oprócz włókien organicznych w skład papieru wchodzą substancje niewłókniste – wypełniacze organiczne : np .
skrobia ziemniaczana i wypełniacze nieorganiczne – mineralne : kaolin , talk , gips , kreda oraz niekiedy substancje chemiczne typu hydrosulfit oraz barwniki .
Wypełniacze poprawiają właściwości papieru ( gładkość , samozerwalność , nieprzezroczystość , białość , odcień ) .
Rodzaj włókien , wypełniaczy oraz proporcje ich użycia określa `` receptura papieru '' , zależna od rodzaju i przeznaczenia papieru .
Historia papieru na świecie .
Papier ( według chińskich kronik ) został wynaleziony w Chinach przez kancelistę na dworze cesarza He Di z dynastii Han , eunucha Cai Lun , około 105 r. n.e .
Kancelista eksperymentował z korą drzew , jedwabiem , a nawet sieciami rybackimi , aż trafił na właściwą metodę ( papier czerpany ) z użyciem szmat jedwabnych i lnianych .
Cesarz He Di w uznaniu doniosłości wynalazku podniósł go do godności ministra rolnictwa .
Wyniki badań archeologicznych pokazują jednak , że papier był już znany wcześniej , co najmniej w 8 roku p.n.e .
Z tego roku pochodzi skrawek papieru z 20 chińskimi znakami odnaleziony w Nefrytowej Bramie , granicznej strażnicy jedwabnego szlaku .
