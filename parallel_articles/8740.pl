Paproty
Paproty – ( niem .
`` Parpart , Kreis Schlawe/Pommern '' ) wieś w Polsce położona w województwie zachodniopomorskim , w powiecie sławieńskim , w gminie Malechowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa koszalińskiego .
Zarys historii .
Wieś o metryce średniowiecznej , w źródłach wymieniona w 1262 , 1274 , 1275 , 1290 , 1296 , 1308 , 1319 i w źródłach nowożytnych .
