Papua-Nowa Gwinea
Papua-Nowa Gwinea ( , Niezależne Państwo Papui-Nowej Gwinei – ) – po Australii , drugie co do wielkości państwo w Oceanii , leży w Melanezji , położone w większości na wyspie Nowa Gwinea .
Papua-Nowa Gwinea graniczy z Indonezją ( 820 km ) .
Powierzchnia państwa wynosi 462,8 tys .
km² .
W 2017 roku w Papui-Nowej Gwinei mieszkało 6 909 701 osób .
Statusem urzędowym objęte są języki : angielski , tok pisin , hiri motu .
Stolicą państwa jest Port Moresby .
Walutą jest kina .
Papua-Nowa Gwinea uzyskała niepodległość 16 września 1975 roku .
Papua-Nowa Gwinea jest członkiem : Azjatyckiego Banku Rozwoju , Banku Światowego , Forum Wysp Pacyfiku , Międzynarodowej Organizacji Pracy , Międzynarodowego Funduszu Walutowego , Organizacji Narodów Zjednoczonych , Światowej Organizacji Handlu i Współpracy Ekonomicznej Azji i Pacyfiku .
Geografia .
Papua-Nowa Gwinea położona jest we wschodniej części Nowej Gwinei oraz na sąsiednich archipelagach ( przede wszystkim Archipelagu Bismarcka i Wyspach Salomona ) .
Rzeźba wysp jest silnie rozczłonkowana .
Centralną część Nowej Gwinei , na której znajduje się Papua-Nowa Gwinea , zajmują Góry Centralne – łańcuch górski ciągnący się przez całą wyspę .
Najwyższym szczytem jest Góra Wilhelma ( 4509 m ) leżąca w Górach Bismarcka , jednego z pasm Gór Centralnych .
Do Gór Centralnych od południowego zachodu przylega płaska , rozległa i miejscami silnie zabagniona nizina .
Bagna na nizinach są jednymi z największych na świecie .
Wyspy przy północnym wybrzeżu Nowej Gwinei oraz Nowa Brytania leżą na obszarze sejsmicznym .
W Papui-Nowej Gwinei znajduje się 18 aktywnych wulkanów ( m.in .
na wyspach : Long , Karkar , Manam ) .
W Papui-Nowej Gwinei często występują trzęsienia ziemi .
Sieć rzeczna w Papui-Nowej Gwinei jest dobrze rozwinięta .
Najdłuższymi rzekami są : Sepik , Ramu , Fly , Purari .
Na Papui-Nowej Gwinei występuje klimat równikowy ( wilgotny , w górach podrównikowy wilgotny ) .
Średnia temperatura dobowa na nizinach wynosi ok. 26 °C , a na terenach górzystych ( 2500–3000 m ) mniej niż 10 °C .
Roczna suma opadów waha się od 1500 do 7500 mm .
Od grudnia do marca występuje pora bardziej wilgotna , która wiąże się z monsunem północno-zachodnim .
Mniej wilgotna pora występuje od maja do października .
Na wysokości 2500–3000 m występują ciągłe opady drobnego deszczu ( niekiedy gradu ) .
W Papui-Nowej Gwinei występują lasy równikowe , a na wysokości 1500–3000 m lasy górskie .
Zarośla i łąki wysokogórskie znajdują się pod górną granicą lasu na Nowej Gwinei .
Na niskich brzegach morskich oraz przy ujściach rzek znajdują się namorzyny .
W dorzeczu rzeki Fly występuje sawanna z akacjami i eukaliptusami .
Lesistość wynosi 63 % .
Wzdłuż wybrzeży występują rafy koralowe .
Podział administracyjny .
Papua-Nowa Gwinea dzieli się na 18 prowincji , jeden region autonomiczny oraz jeden dystrykt stołeczny .
Historia .
Europejczycy pojawili się na terenach dzisiejszej Papui-Nowej Gwinei w XVI w. W 1884 roku wyspy ogłoszono protektoratami Niemiec ( część północna – Nowa Gwinea Niemiecka ) i Wielkiej Brytanii ( część południowa – Nowa Gwinea Brytyjska ) .
W 1906 roku Australia objęła administrację w brytyjskiej części ( nazwanej Terytorium Papui ) , a w 1914 roku , po wybuchu I wojny światowej , również w niemieckiej .
W 1942 roku wyspy zajęła Japonia .
We wrześniu 1945 roku alianci wyparli ostatnie wojska japońskie z wysp .
W 1946 roku Terytorium Nowej Gwinei ogłoszono terytorium powierniczym Organizacji Narodów Zjednoczonych , którym administrowała Australia .
W 1949 roku Australia połączyła obie części w jednolite związkowe terytorium zamorskie o nazwie Papua-Nowa Gwinea .
W 1951 roku rozpoczęto przekazywanie lokalnym organom zarządzanie sprawami wewnętrznymi .
Powołano Radę Ustawodawczą , którą w 1964 roku zastąpiono wybieraną przez mieszkańców wysp Izbą Zgromadzeń .
W grudniu 1973 roku Papua-Nowa Gwinea uzyskała pełną autonomię wewnętrzną , a 16 września 1975 roku ogłosiła niepodległość , przyjęła konstytucję oraz została członkiem Organizacji Narodów Zjednoczonych i brytyjskiej Wspólnoty Narodów .
