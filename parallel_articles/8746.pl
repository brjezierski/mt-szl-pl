Parafianowo ( stacja kolejowa )
Parafianowo ( , , ros .
nazwa normatywna Парафьяново ) – stacja kolejowa w miejscowości Parafianowo , w rejonie dokszyckim , w obwodzie witebskim , na Białorusi .
Położona jest na linii Połock - Mołodeczno .
Jest to najbliżej położona stacja kolejowa dla stolicy rejonu Dokszyc .
Stacja powstała przed I wojną światową .
