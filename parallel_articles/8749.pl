Paramore ( album )
Paramore – czwarty album studyjny zespołu Paramore wydany 5 kwietnia 2013 przez wytwórnię Fueled by Ramen , pierwszy album nagrany bez współzałożycieli zespołu , Josha i Zaca Farro .
Pierwszy album Paramore , który zadebiutował jako numer 1 na Billboard 200 .
W wywiadzie dla `` Radio.com '' , Hayley Williams , na pytanie dlaczego album nazywa się jakby był debiutancki , odpowiedziała : `` Debiutancki aspekt tej sprawy jest zdecydowanie pewnym manifestem .
Czuję , że to nie tylko ponownie wprowadza zespół w świat , ale nawet pomiędzy nas samych .
[ ... ] Pod koniec wyglądało to tak , jakbyśmy byli zupełnie nowym zespołem '' .
