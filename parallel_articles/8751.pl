Paranaguá
Paranaguá – miasto w południowej Brazylii , nad Oceanem Atlantyckim , w stanie Parana .
Paranaguá została założona w 1648 roku i jest najstarszym miastem stanu Paraná .
Odległość od stolicy stanu Paraná , Kurytyby , wynosi 91 km .
Liczba mieszkańców : 147 934 ( 2004 ) .
Według IBGE ( Brazylijskiego Instytutu Geografii i Statystyki ) spis w 2010 roku wykazał 140 469 mieszkańców .
W mieście znajduje się ośrodek przemysłu drzewnego oraz spożywczego .
W mieście ma również siedzibę klub piłkarski Rio Branco Sport Club .
