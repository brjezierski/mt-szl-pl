Parczew ( przystanek kolejowy )
Parczew ( , ) – przystanek kolejowy w pobliżu Jeziora Iłga ( ) , w rejonie święciańskim , na Litwie .
Leży na linii dawnej Kolei Warszawsko-Petersburskiej .
Przystanek powstał w XIX w. pomiędzy stacjami Bezdany a Podbrodzie .
Nosił wówczas nazwę Drużyły ( Drużyle ) .
W okresie międzywojennym nazywał się Parczew .
