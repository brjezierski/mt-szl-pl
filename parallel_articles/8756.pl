Pardubice
Pardubice ( niem .
`` Pardubitz '' ) – miasto statutarne w środkowej części Czech , w Kotlinie Pardubickiej , u zbiegu rzek Chrudimki i Łaby .
Jest stolicą kraju pardubickiego oraz powiatu Pardubice .
Na początku 2020 roku , z liczbą mieszkańców wynoszącą ponad 90 tys. , Pardubice zajmowały dziesiąte miejsce pod względem liczby ludności w Republice Czeskiej .
Geografia .
Pardubice leżą nad drugą pod względem wielkości rzeką Czech , Łabą , do której wpada inna rzeka w mieście - Chrudimka .
Położenie Pardubic określają współrzędne 15° długości geograficznej wschodniej i 50° szerokości geograficznej północnej .
Miasto znajduje się 100 km na wschód od stolicy Pragi , 150 km w kierunku północno-zachodnim od Brna .
Pardubice położone są na obszarze Niziny Połabskiej o średniej wysokości 225 m n.p.m .
Powierzchnia miasta obejmuje 78 km² .
Powierzchnia składa się przeważnie z nizin i nie ma wzgórz .
