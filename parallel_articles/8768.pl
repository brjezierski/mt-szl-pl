Parasol ( skała )
Parasol – turnia w Grupie Parasola na orograficznie lewym zboczu Doliny Szklarki , w obrębie miejscowości Jerzmanowice w województwie małopolskim , w powiecie krakowskim , w gminie Jerzmanowice-Przeginia .
Jest to obszar Wyżyny Olkuskiej będącej częścią Wyżyny Krakowsko-Częstochowskiej .
Parasol znajduje się na zachodnim zboczu w rzadkim , bukowym lesie .
Uprawiana jest na nim wspinaczka skalna .
Jest to zbudowana z twardego wapienia skalistego samotna , strzelista turnia najniżej położona wśród czterech wspinaczkowych skał Grupy Parasola .
Ma wysokość 10 m , ściany pionowe lub przewieszone .
Do 2020 r. wspinacze przeszli na niej tylko jedną łatwą drogę wspinaczkową ( `` Droga wejściowa '' , II w skali Kurtyki ) , 2 spity .
Są jeszcze 3 projekty .
Pozostałe skały Grupy Parasola to : Turnia nad Parasolem , Kuferek i Skarbiec .
Wszystkie znajdują się w lesie .
