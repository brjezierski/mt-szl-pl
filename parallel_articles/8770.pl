Pas ( brydż )
Pas – zapowiedź zgłaszana podczas licytacji oznaczająca tyle co „ nie licytuję , kontynuujcie ” .
Gracz w momencie , gdy przyjdzie jego kolei licytowania , może powiedzieć `` pas '' rezygnując ze zgłoszenia odzywki , kontry lub rekontry .
Nie wyklucza go to z udziału w licytacji w następnych okrążeniach .
Cztery `` pasy '' na początku licytacji kończą dane rozdanie .
Trzy `` pasy '' po dowolnej odzywce kończą licytacje , a ostatnio zgłoszona odzywka staje się kontraktem .
Zatem , w sytuacji gdy przeciwnicy nie licytują , `` pas '' po odzywce partnera zamyka licytacje i powoduje przerwanie wymiany informacji między partnerami .
Aby zapobiec takiej sytuacji partnerzy stosują odzywki forsujące , które zobowiązują do dalszej licytacji .
