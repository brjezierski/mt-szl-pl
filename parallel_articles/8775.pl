Pasig
Pasig – miasto na Filipinach , w środkowej części wyspy Luzon , u ujścia rzeki Mariquina do rzeki Pasig , w południowo-wschodniej części zespołu miejskiego Metro Manila , stolica prowincji Rizal .
W 2010 roku jego populacja liczyła 669 773 mieszkańców .
W mieście rozwinął się przemysł spożywczy .
