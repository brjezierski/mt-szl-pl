Paso Robles
Paso Robles ( także El Paso de Robles ) – miasto ( `` city '' ) w hrabstwie San Luis Obispo , w południowo-zachodniej części stanu Kalifornia , w Stanach Zjednoczonych , położone nad rzeką Salinas .
W 2013 roku miasto liczyło 30 857 mieszkańców .
Paso Robles położone jest w regionie winiarskim .
W okolicy znajdują się także źródła termalne .
Nazwa miasta oznacza w języku hiszpańskim „ dębową przełęcz ” .
