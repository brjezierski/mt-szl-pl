Paso de los Toros
Paso de los Toros – miasto w Urugwaju , w departamencie Tacuarembó .
W 2004 roku miasto liczyło 13,459 mieszkańców .
Paso de los Toros znajduje się na północnym brzegu Río Negro .
W 1889 roku na terenach miasta znajdowała się trasa kolejowa łącząca miejscowość Paso de los Toros i Salto .
Paso de los Toros prawa miejskie otrzymało 1 lipca 1953 roku .
