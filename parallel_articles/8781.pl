Passalora fulva
Passalora fulva ( Cooke ) U. Braun & amp ; Crous – gatunek grzybów z klasy `` Dothideomycetes '' .
U pomidorów wywołuje chorobę o nazwie brunatna plamistość liści pomidora .
Gatunek kosmopolityczny , występujący poza Antarktydą na wszystkich kontynentach , a także na wielu wyspach .
Pasożytuje głównie na pomidorze .
Rozprzestrzenia się przez zarodniki , które mogą być przenoszone przez wiatr lub krople deszczu .
Mogą one przetrwać na nasionach , resztkach roślin lub na konstrukcjach szklarniowych nawet do 12 miesięcy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Passalora , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1878 r. Mordecai Cubitt Cooke nadając mu nazwę `` Cladosporium fulvum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Uwe Braun i P.W .
Crous w 2003 r .
