Pasawa
Pasawa ( niem .
`` Passau '' , łac .
`` Castra Batavia '' , czes .
`` Pasov '' ) – miasto na prawach powiatu w południowo-wschodnich Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Bawaria , w regionie Donau-Wald , siedziba powiatu Pasawa .
Leży bezpośrednio przy granicy z Austrią .
Liczy 49 454 mieszkańców ( 31.12.2013 ) i jest ważnym ośrodkiem gospodarczym , kulturalnym , administracyjnym i turystycznym , węzłem komunikacyjnym oraz siedzibą uniwersytetu .
Osadnictwo na obszarze miasta sięga czasów neolitu .
Miasto w obecnym kształcie wywodzi się z rzymskiego obozu warownego , który w VIII w. stał się siedzibą biskupstwa .
Prawa miejskie Pasawa otrzymała w roku 1225 .
Przez całe średniowiecze jej historia była ściśle związana z kościołem – biskupi pasawscy byli jednocześnie władcami księstwa , którego miasto było stolicą .
W czasach nowożytnych najważniejszym wydarzeniem był wielki pożar , do którego doszło w 1662 r. Odbudowa , przy której pracowali architekci przede wszystkim z Włoch , nadała starówce z grubsza obecny wygląd .
Ostatnie dwieście lat to także okres wielkiej prosperity Pasawy – rozwój przemysłu , węzeł kolejowy , powstanie uniwersytetu .
Szczęśliwie miasto uniknęło zniszczeń w czasie II wojny światowej .
Pasawa położona jest na licznych wzgórzach rozdzielonych trzema rzekami zbiegającymi się u stóp Starego Miasta – Dunajem , Inn i Ilz .
