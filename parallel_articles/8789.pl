Patak
Patak – wieś i gmina w północnej części Węgier , w pobliżu miasta Balassagyarmat .
Miejscowość leży na obszarze Średniogórza Północnowęgierskiego , w pobliżu granicy ze Słowacją .
Administracyjnie osada należy do powiatu Balassagyarmat , wchodzącego w skład komitatu Nógrád .
Gmina Patak liczy 925 mieszkańców ( 2009 r. ) i zajmuje obszar 16,22 km² .
