Patellaria atrata
Patellaria atrata ( Hedw . )
Fr .
– gatunek grzybów z rodziny ( `` Patellariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Patellariaceae , Patellariales , incertae sedis , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1793 Johann Hedwig nadając mu nazwę `` Lichen atratus '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu w roku 1822 Elias Magnus Fries przenosząc go do rodzaju `` Patellaria '' .
