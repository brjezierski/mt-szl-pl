staroż .
, `` Pátrai '' ) – trzecie pod względem wielkości miasto Grecji ( największe na Peloponezie ) , w administracji zdecentralizowanej Peloponez , Grecja Zachodnia i Wyspy Jońskie , siedziba administracyjna regionu Grecja Zachodnia , jednostki regionalnej Achaja oraz gminy Patras .
