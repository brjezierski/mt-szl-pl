Leży nad Zatoką Patraską , drugi co do wielkości grecki port morski ( po Pireusie ) .
Posiada połączenia promowe z Wyspami Jońskimi i Włochami ( Ankoną , Brindisi , Bari , Triestem i Wenecją ) .
