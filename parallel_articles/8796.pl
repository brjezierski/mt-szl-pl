Patricia McCormick
Patricia McCormick ( ur .
12 maja 1930 w Seal Beach , zm .
7 marca 2023 ) , amerykańska skoczkini do wody .
Wielokrotna medalistka olimpijska .
Na dwóch olimpiadach zdominowała rywalizację w swojej dyscyplinie .
W 1952 zdobyła złote medale w obu olimpijskich konkurencjach – skokach z wieży i platformy .
Cztery lata później powtórzyła ten wyczyn .
Podobnym osiągnięciem może się pochwalić jedynie , w rywalizacji mężczyzn , Greg Louganis .
Zdobyła również cztery medale igrzysk panamerykańskich , podczas zmagań w 1951 i 1955 roku .
Medalistką olimpijską była także jej córka , Kelly .
