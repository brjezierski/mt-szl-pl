Patrick Swayze
Patrick Wayne Swayze ( ur .
18 sierpnia 1952 w Houston , zm .
14 września 2009 w Los Angeles ) – amerykański aktor filmowy i telewizyjny , tancerz , piosenkarz i choreograf .
18 sierpnia 1997 otrzymał gwiazdę w Alei Gwiazd w Los Angeles znajdującą się przy 7018 Hollywood Boulevard .
Życiorys .
Wczesne lata .
Urodził się jako najstarszy syn i jedno z pięciorga dzieci Yvonne Helen Karnes Swayze ( 1927–2013 ) , tancerki–choreografki , i Jessiego Wayne ’ a Swayzego ( 1925–1982 ) , kreślarza .
Jego rodzina miała pochodzenie angielskie , irlandzkie , szkockie i holenderskie .
Miał dwóch młodszych braci : Donalda Carla ( ur .
10 sierpnia 1958 ) i Seana Kyle ’ a ( ur .
13 października 1962 ) , oraz dwie siostry : Bambi i Vicky Lynn ( 1949–1994 ) .
Uczęszczał do szkoły Black Middle School w Oak Forest w stanie Illinois .
Edukację kontynuował w Houston w stanie Teksas w katolickiej szkole im .
Świętej Róży z Limy , liceum Waltrip High School oraz San Jacinto College .
