Patriotyzm
Patriotyzm ( łac .
`` patria '' – ojczyzna , gr .
`` patriotes '' ) – postawa szacunku , umiłowania i oddania własnej ojczyźnie oraz chęci ponoszenia za nią ofiar i gotowości do jej obrony .
Charakteryzuje się przedkładaniem celów ważnych dla ojczyzny nad osobiste , gotowością do pracy dla jej dobra i w razie potrzeby , poświęcenia dla niej własnego zdrowia lub nawet życia .
Patriotyzm to również umiłowanie i pielęgnowanie narodowej tradycji , kultury czy języka .
Oparty jest na poczuciu więzi społecznej , wspólnoty kulturowej oraz solidarności z własnym narodem i społecznością .
Etymologia i znaczenie .
Termin „ patriotyzm ” pochodzi od greckiego słowa „ πατριά ” – „ patria ” , oznaczającego rodzinę , grupę , plemię lub naród wywodzący się od wspólnych przodków , ojców , ojczyznę .
„ Patria ” jest z kolei derywatem źródłowej nazwy greckiej „ πατήρ ” – „ patēr ” oznaczającej „ ojca ” – głowę rodu .
Te greckie terminy zostały przejęte później przez Rzymian i przeniknęły do łaciny w formie „ pater ” i „ patrio ” , rozpowszechniając się także w innych europejskich językach .
Słowo „ patriotyzm ” dosłownie oznacza „ miłość ojczyzny ” .
Łaciński termin `` patria '' znany był w Polsce już w końcu średniowiecza dzięki pierwszym polskim słownikom łacińsko-polskim .
Wymienia go w 1532 słownik łacińsko-polski Bartłomieja z Bydgoszczy – „ Patria est natale solum – myesscze vrodzenya oyczyzna ” .
Słownik ten podaje także pierwsze rodzime staropolskie określenie w tłumaczeniu znaczenia łacińskiego wyrażenia patrimonium – „ ojcowizność ” w oryg .
„ Patrimonium ( ... ) oiczyzna , oiczovyznoscz vel est bona paterna ” .
Historia .
Patriotyzm jako idea rozwinął się w czasach oświecenia .
