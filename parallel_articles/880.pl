A Coruña ( prowincja )
A Coruña ( hiszp .
`` Provincia de A Coruña '' , gal .
`` Provincia da Coruña '' ) – prowincja w północno-zachodniej Hiszpanii , współtworząca wspólnotę autonomiczną Galicji .
Stolicą jest miasto A Coruña .
Graniczy z prowincjami Lugo i Pontevedra , położona jest nad Oceanem Atlantyckim .
