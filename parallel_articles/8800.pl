Patryk Dziczek
Patryk Dziczek ( ur .
25 marca 1998 w Gliwicach ) – polski piłkarz grający na pozycji pomocnika .
Kariera klubowa .
Piast Gliwice .
Jest wychowankiem Piasta Gliwice .
W Ekstraklasie zadebiutował 5 czerwca 2015 w przegranym 0:3 meczu z Cracovią .
Grał do 46. minuty , po czym został zmieniony przez Pawła Moskwika .
W sezonie 2018/2019 zdobył wraz ze swoją drużyną tytuł Mistrza Polski .
Wystąpił w dwóch meczach I rundy kwalifikacyjnej do Ligi Mistrzów UEFA oraz w dwóch meczach II rundy kwalifikacyjnej do Ligi Europy UEFA .
S.S. Lazio .
22 sierpnia 2019 Patryk Dziczek został zawodnikiem S.S. Lazio .
Pomocnik podpisał z włoskim klubem piłkarskim pięcioletni kontrakt .
US Salernitana 1919 .
28 sierpnia 2019 Patryk Dziczek został wypożyczony do US Salernitana 1919 .
W drużynie „ Koników morskich ” rozegrał on 18 spotkań , w których zdobył 1 gola oraz zanotował 2 asysty .
Powrót do Piasta .
31 sierpnia 2022 Piast Gliwice poinformował , że Patryk Dziczek po trzech latach wrócił do Gliwic .
Zawodnik podpisał umowę do 30 czerwca 2025 .
