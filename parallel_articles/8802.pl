Pau ( Alt Empordà )
Pau – gmina w Hiszpanii , w Katalonii , w prowincji Girona , w comarce Alt Empordà .
Powierzchnia gminy wynosi 10,69 km² .
Zgodnie z danymi INE , w 2005 roku liczba ludności wynosiła 454 , a gęstość zaludnienia 42,47 osoby/km² .
Wysokość bezwzględna gminy równa jest 33 metry .
Kod pocztowy do gminy to 17494 .
