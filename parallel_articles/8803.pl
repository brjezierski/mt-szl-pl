Życiorys .
Rodzina .
Paul Ehrlich urodził się w żydowskiej rodzinie jako drugie dziecko .
Jego ojciec , Ismar Ehrlich , był żonaty z Rosą z domu Weigert , był producentem napojów alkoholowych i agentem loteryjnym w Strzelinie na Dolnym Śląsku .
Jego dziadek , Heymann Ehrlich , był miejscowym gorzelnikiem i dzierżawcą i osiągnął pewne bogactwo .
Ismar Ehrlich stał na czele gminy żydowskiej .
Paul Ehrlich nie przeszedł na protestantyzm – do czego wielu jego żydowskich przyjaciół czuło się zmuszonych ze względów zawodowych – ale raczej beztrosko przestrzegał żydowskich zwyczajów i przepisów .
Kuzynem Ehrlicha ze strony matki był patolog z Wrocławia Carl Weigert , który pokazał mu techniki barwienia komórek za pomocą substancji chemicznych , co pozwalało prowadzić obserwacje mikroskopowe .
Edukacja .
Po ukończeniu szkoły podstawowej w Strzelinie Paul Ehrlich uczęszczał w latach 1864–1872 do gimnazjum św .
Marii Magdaleny we Wrocławiu , gdzie poznał Alberta Neissera .
Różne historie z dzieciństwa i młodości pozostają spekulacyjne .
