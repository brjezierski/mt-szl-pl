Paul Newman ( językoznawca )
Paul Newman ( ur .
1937 ) – amerykański językoznawca , specjalista w zakresie języków afrykańskich .
Zajmuje się przede wszystkim językiem hausa oraz językami czadyjskimi .
Do jego zainteresowań naukowo-badawczych należą m.in .
: lingwistyka historyczna , morfofonologia , tonalność , metody badań terenowych , leksykografia .
Posiada bakalaureat z filozofii oraz magisterium z antropologii ( University of Pennsylvania ) .
Doktorat z językoznawstwa uzyskał w 1967 r. na Uniwersytecie Kalifornijskim w Los Angeles .
