Paul Pogba
Paul Labile Pogba ( ur .
15 marca 1993 w Lagny-sur-Marne ) – francuski piłkarz pochodzenia gwinejskiego występujący na pozycji pomocnika we włoskim klubie Juventus oraz w reprezentacji Francji .
Wicemistrz Europy z 2016 roku .
Mistrz Świata z 2018 roku .
Uczestnik Mistrzostw Świata 2014 i Mistrzostw Europy 2021 .
Kariera klubowa .
Początek kariery .
Pogba rozpoczął grę w piłkę nożną w okolicznych paryskich klubach , takich jak US Roissy-en-Brie i US Torcy , a w 2007 podpisał kontrakt z Le Havre AC .
Grał tam przez dwa lata , zanim w 2009 przeniósł się do Manchesteru United .
Manchester United .
7 października 2009 ogłoszono , że Paul Pogba zostanie zawodnikiem Manchesteru United .
