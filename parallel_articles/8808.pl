Paulton
Paulton – wieś w hrabstwie Somerset w Anglii , w jego północno-wschodniej części .
Znajduje się na północy wzgórz Mendip i administrowana jest przez jednostkę administracyjną Bath and North East Somerset .
Wieś zamieszkuje ok. 5000 mieszkańców .
Górnictwo węglowe .
Istnieją dowody na górnictwo węglowe w tej części Somersetu , m.in .
hałda żużlowa `` The Batch '' o kształcie wulkanicznym .
Wieś była centrum co najmniej 15 kopalń – wszystkie z nich zostały zamknięte w XIX wieku .
