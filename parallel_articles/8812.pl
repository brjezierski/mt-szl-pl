Pawelekville
Pawelekville – obszar niemunicypalny w hrabstwie Karnes , w Teksasie , w Stanach Zjednoczonych .
W 2014 był zamieszkany przez 110 osób .
Historia .
Osada została założona przez śląskich imigrantów .
Przed 1947 , była nazywana `` Carvajal Crossing '' , po pobliskim brodzie o tej samej nazwie , znajdującym się nad potokiem Cibolo .
Później nazwa osady została przemianowana na Pawelekville , na cześć rodziny Pawelek .
W 1990 , osada posiadała małą ilość domów , znajdujących się wokół kilku biznesów , które obsługiwały lokalną społeczność rolniczą .
Pomiędzy 1964 i 1965 , Pawlekville liczyło 65 mieszkańców .
Pomiędzy 1970 i 2000 , liczba ludności była szacowana na 105 .
W 2014 Pawelekville było zamieszkane przez 110 osób .
