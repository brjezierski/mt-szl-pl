Paweł Adamowicz
Paweł Bogdan Adamowicz ( ur .
2 listopada 1965 w Gdańsku , zm .
14 stycznia 2019 tamże ) – polski prawnik , radca prawny , samorządowiec i polityk , działacz opozycji demokratycznej w PRL , w latach 1998–2019 prezydent Gdańska .
Był przewodniczącym studenckiego komitetu strajkowego na Uniwersytecie Gdańskim podczas strajków w 1988 .
W latach 1990–1993 był prorektorem Uniwersytetu Gdańskiego .
W 1990 roku uzyskał mandat radnego Gdańska .
W latach 1994–1998 był przewodniczącym Rady Miasta Gdańska .
W latach 1998–2019 był nieprzerwanie prezydentem Gdańska .
Jako prezydent miasta podkreślał wartość różnorodności kulturowej , prowadził politykę otwartą na różne grupy , odwoływał się przy tym do wartości chrześcijańskich .
Podkreślał znaczenie dialogu i pojednania polsko-niemieckiego oraz współpracy z mniejszością kaszubską .
Był zaangażowany we wdrożenie miejskiego programu integracji osób migranckich , wziął udział w gdańskim Marszu Równości .
W działalności samorządowej niejednokrotnie ścierał się ideowo i programowo z oponentami z Prawa i Sprawiedliwości .
W wydanej w 2018 książce `` Gdańsk jako wspólnota '' krytykował politykę PiS jako niedemokratyczną i naruszającą ład konstytucyjny .
Zmarł 14 stycznia 2019 w wyniku ran zadanych nożem po zamachu podczas udziału w finale Wielkiej Orkiestry Świątecznej Pomocy .
