Paweł Królikowski
Paweł Zbigniew Królikowski ( ur .
1 kwietnia 1961 w Zduńskiej Woli , zm .
27 lutego 2020 w Warszawie ) – polski aktor filmowy , telewizyjny , teatralny i dubbingowy , prezenter i realizator telewizyjny oraz działacz kulturalny , w latach 2018–2020 prezes Związku Artystów Scen Polskich .
Życiorys .
Wczesne lata .
Urodził się i dorastał w Zduńskiej Woli jako najstarsze z trojga dzieci pary nauczycieli – Jadwigi i Jana Królikowskich .
Jego ojciec działał też społecznie w szkolnych klubach sportowych , przez 19 lat był prezesem zarządu miejskiego SKS .
Praprababka Teresa Królikowska z domu Breier ( 1840–1886 ) w połowie XIX wieku przybyła z Czech do Zduńskiej Woli .
Miał młodszego brata Rafała ( ur .
1966 ) , który został także zawodowym aktorem , i młodszą siostrę Anitę ( ur .
1975 ) .
Od dziecka wykazywał zainteresowania aktorsko-teatralne .
Chodził na zajęcia teatralne do Miejskiego Domu Kultury w Zduńskiej Woli .
Pisał wiersze i scenariusze .
Uczęszczał do zduńskowolskiej Szkoły Podstawowej nr 3 .
Po skończeniu II Liceum Ogólnokształcącego im .
Jana Pawła II w Zduńskiej Woli zdawał do szkoły teatralnej , ale zapomniał wziąć ze sobą świadectwo dojrzałości , przez co nie został dopuszczony do egzaminów .
