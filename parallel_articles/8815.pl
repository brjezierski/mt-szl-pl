Paweł Kukiz
Paweł Piotr Kukiz ( ur .
24 czerwca 1963 w Paczkowie ) – polski muzyk , wokalista , autor tekstów piosenek i polityk .
W latach 1984–2013 lider zespołu Piersi .
Kandydat w wyborach prezydenckich w 2015 , inicjator komitetu wyborczego i ruchu politycznego Kukiz ’ 15 , poseł na Sejm VIII i IX kadencji , przewodniczący klubu poselskiego Kukiz ’ 15 w Sejmie VIII kadencji i koła poselskiego Kukiz ’ 15 – Demokracja Bezpośrednia w Sejmie IX kadencji .
Życiorys .
Życie prywatne .
Wnuk Mariana Kukiza , przodownika Policji Państwowej w II Rzeczypospolitej , zamordowanego w czerwcu 1941 w ramach masakr więziennych NKWD we Lwowie .
Syn Marianny oraz Tadeusza , lekarza i działacza kresowego .
Od 1991 jest żonaty z Małgorzatą , ma trzy córki : Julię ( ur .
1991 ) , Polę ( ur .
1994 ) i Hannę ( ur .
2000 ) .
Z zawodu muzyk , uzyskał wykształcenie średnie ogólne .
W Niemodlinie ukończył szkołę podstawową i liceum ogólnokształcące ( 1981 ) , tam też został założony zespół Piersi ( 1984 ) .
Studiował administrację na Uniwersytecie Wrocławskim , a następnie prawo i nauki polityczne na Uniwersytecie Warszawskim , nie kończąc żadnego z tych kierunków .
Kariera muzyczna i rozrywkowa .
W latach 80. udzielał się muzycznie w różnych zespołach rockowych , takich jak CDN , Hak oraz Aya RL .
W latach 1984–2013 był liderem zespołu Piersi , w latach 1997–2004 działającego pod nazwą Kukiz i Piersi .
Z grupą tą wydał dziewięć płyt .
Kontrowersje w tym czasie wzbudził m.in .
antykościelnym utworem „ ZChN zbliża się ” , któremu zarzucano obrazę uczuć religijnych .
Od 1989 był wokalistą zespołu Emigranci .
W 2001 wziął udział w projekcie muzycznym Yugoton , a w 2007 uczestniczył w jego kontynuacji – Yugopolis .
W 2003 wydał płytę pt .
`` Borysewicz & amp ; Kukiz '' w duecie z Janem Borysewiczem , z którym wcześniej nagrał także ścieżkę dźwiękową do filmu Olafa Lubaszenki `` E=mc² '' z 2002 .
W 2010 z Maciejem Maleńczukiem nagrał płytę z repertuarem Kabaretu Starszych Panów zatytułowaną `` Starsi panowie '' .
W duecie z Pihem zaśpiewał piosenkę „ Młodość ” na wydanym w 2014 albumie `` Bal maturalny '' produkcji Rozbójnika Alibaby oraz Jana Borysewicza .
W latach 90. zaczął pojawiać się w filmach – grał główne role w filmie sensacyjnym `` Girl Guide '' Juliusza Machulskiego , a z Bolcem w produkcjach `` Poniedziałek '' i `` Wtorek '' Witolda Adamka .
W drugiej połowie lat 90. wystąpił w reklamie napoju Pepsi .
Jak sam stwierdził , promował tę markę „ dla pieniędzy ” .
Do wydarzenia nawiązała grupa Kazik na Żywo w utworze „ Pozory często mylą ” wydanym na albumie `` Las Maquinas de la Muerte '' ( 1999 ) , w którym pojawiły się słowa stanowiące bezpośrednie odniesienie do Pawła Kukiza ( `` Spragniony , do picia nieledwie się zbliżam , wiedziony reklamą z udziałem Kukiza '' ) .
Od 2009 był współgospodarzem ( z Markiem Horodniczym ) programu publicystycznego `` Koniec końców '' w TVP1 .
