Paweł Targiel
Paweł Targiel ( ur .
4 czerwca 1945 w Mikołowie , zm .
11 czerwca 2009 tamże ) – polski poeta i działacz kultury związany ze Śląskiem .
W 1988 założył ukazujące się początkowo w drugim obiegu czasopismo „ Miarka ” , propagujące idee demokratyczne , a w 1996 „ Arkadię .
Pismo katastroficzne ” .
Był inicjatorem przekształcenia mieszkania , w którym młodość spędził Rafał Wojaczek w instytucję kultury – Instytut Mikołowski , co nastąpiło w 1999 roku , a następnie został dyrektorem tej placówki .
Wydał dwa tomiki własnych wierszy : `` Ostatnie wiersze '' ( 2005 ) i `` Halsem '' ( 2007 ) .
