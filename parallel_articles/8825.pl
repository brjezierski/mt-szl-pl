Paysandú ( departament )
Paysandú – urugwajski departament położony na zachodzie kraju .
Rzeka
Urugwaj wyznacza zachodnią granicę z argentyńską prowincją Entre Ríos .
Ponadto Paysandú sąsiaduje z następującymi departamentami : na północy z Salto , na wschodzie z
Tacuarembó , a na południu z Río Negro .
Ośrodkiem administracyjnym oraz największym miastem tego powstałego w 1820 r. departamentu jest Paysandú nad rzeką Urugwaj .
Powierzchnia Paysandú wynosi 13 922 km² .
W 2004 r. departament ten zamieszkiwało 113 244 osoby , co dawało gęstość zaludnienia 8,1 mieszk./km² .
