Paços de Ferreira
Paços de Ferreira ( wym . )
– miejscowość w Portugalii , leżąca w dystrykcie Porto , w regionie Północ w podregionie Tâmega .
Miejscowość jest siedzibą gminy o tej samej nazwie .
W mieście siedzibę ma klub piłkarski FC Paços Ferreira , uczestniczący w sezonie 2006/2007 w rozgrywkach BWINligi .
