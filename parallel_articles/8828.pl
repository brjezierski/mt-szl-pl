Pałac w Mosznej
Pałac w Mosznej ( niem .
`` Schloss Moschen '' ) – zabytkowa rezydencja położona we wsi Moszna , w województwie opolskim , pomiędzy miastami Prudnik i Krapkowice .
W latach 1866–1945 pałac był rezydencją śląskiego rodu Tiele-Wincklerów , potentatów przemysłowych .
W latach 1996–2013 Centrum Terapii Nerwic .
Pałac jest częściowo udostępniony do zwiedzania .
W galerii odbywają się koncerty muzyki kameralnej oraz wystawy dzieł sztuki .
W obiekcie odbywa się doroczne `` Muzyczne Święto Kwitnących Azalii '' – w maju i czerwcu jest tu popularyzowana muzyka polskich i europejskich kompozytorów , w czasie święta galeria prezentuje wystawy plastyczne , odbywają się tu również plenery malarskie .
Architektura .
Pałac posiada 365 pomieszczeń i 99 wież i wieżyczek .
Powierzchnia wynosi 8 tys .
m² , a kubatura – 65 tys .
m³ .
Park .
Obiekt otacza ponad stuhektarowy park , który jest częścią parku krajobrazowego z cennym drzewostanem ( m.in .
trzystuletnie okazy dębów ) i rzadkimi skupiskami rododendronów .
Jedynie oś główna całego konceptu parku ma geometryczny charakter .
Tuż za fontanną znajduje się aleja lipowa na końcu której znajduje marmurowy cokół – pozostałość po pomniku Huberta von Tiele– Wincklera .
Po obu stronach alei zachowały się XIX-wieczne kanały wodne , zbudowane w stylu holenderskim i francuskim oraz malownicze stawy .
Historia .
Badania archeologiczne wykazały istnienie w miejscu obecnego pałacu śladów średniowiecznej , drewnianej budowli .
W ogrodzie pałacowym odkryto na początku XX wieku fragmenty piwnic , które H. Barthel w 1929 roku utożsamił , raczej bezzasadnie , z zamkiem templariuszy , miał on łączyć się podziemiami z zamkiem w Chrzelicach .
Brak informacji o istnieniu pałacu przed 1845 .
Na podstawie analizy przypałacowego parku uznano , że już w XVIII istniało tu założenie pałacowo-ogrodowe , choć sam budynek mógł być wówczas skromnym dworem .
Barokowy pałac w Mosznej był po raz pierwszy wzmiankowany w pracy Johanna Georga Knie z 1845 .
W tym czasie właścicielami Mosznej była rodzina Seherr-Thoss .
W 1853 majątek kupił Heinrich von Erdmansdorf , a w 1866 przemysłowiec Hubert von Tiele-Winckler .
Moszna stała się główną rezydencją rodziny Tiele-Wincklerów .
Po śmierci Huberta , majątek w Mosznej odziedziczył jego najstarszy syn – Franz Hubert , starosta powiatu prudnickiego .
Na mapie okolic Prudnika z drugiej połowy XIX wieku zaznaczony jest budynek na planie prostokąta z silnie wysuniętymi ryzalitami środkowymi , usytuowany prostopadle do osi parkowej .
W nocy z 2 na 3 czerwca 1896 z nieokreślonych przyczyn w Mosznej wybuchł pożar .
Barokowy pałac wówczas częściowo spłonął .
Franz Hubert przystąpił do odbudowy pałacu .
Budowa obecnego obiektu zrealizowana została w dwóch fazach .
Pierwsza , przypadająca na lata 1896–1900 , obejmowała odbudowę spalonej części środkowej oraz dobudowę masywu skrzydła wschodniego w stylu neogotyckim .
Faza druga , czyli lata 1913–1914 , to dobudowa skrzydła zachodniego w stylu neorenesansowym .
Początkowo członkowie rodu Tiele-Wincklerów mieli być chowani w krypcie pod pałacową kaplicą .
Z uwagi na to , że panowała tam zbyt duża wilgoć , podjęto decyzję o przeniesieniu miejsca rodowych pochówków na mieszczący się w pałacowym parku cmentarz .
Jest on udostępniony zwiedzającym .
W 1904 , 1911 oraz 1912 roku gościem podczas polowań urządzanych przez Franza Huberta von Tiele-Wienckler był cesarz Wilhelm II .
Podczas II wojny światowej dostosowano niektóre pomieszczania pałacu dla potrzeb filii lazaretu w Prudniku .
Pałac nie ucierpiał w wyniku działań wojennych .
W 1945 w pałacu kwaterowały oddziały armii radzieckiej .
W tym okresie dewastacji uległa większa część wyposażenia pałacowego oraz wywieziono większość przechowywanych tam dzieł sztuki , głównie obrazów i rzeźb
Po wojnie umieszczano w pałacu na krótko różne instytucje , dopiero w 1972 roku stał się siedzibą sanatorium , a od 1996 roku Samodzielnego Publicznego Zakładu Opieki Zdrowotnej Centrum Terapii Nerwic .
W 1948 roku na części gruntów dawnego majątku powstała Państwowa Stadnina Koni .
Od 2013 funkcjonuje jako spółka z ograniczoną odpowiedzialnością , która została utworzona na mocy uchwały Sejmiku Województwa Opolskiego z 25 czerwca 2013 .
Od 2013 w pałacu funkcjonuje hotel i restauracja .
Szpital został przeniesiony do budynku nieopodal .
W ferie zimowe oraz wakacje letnie w pałacu odbywają się tematyczne kolonie letnie dla dzieci i młodzieży .
W latach 2021-2022 przy elewacji pałacu zrealizowano prace renowacyjne .
Remont realizowany był przez Województwo Opolskie w ramach unijnych funduszy z polsko-czeskiego programu Interreg V-A .
Odniesienie w kulturze .
W 1978 roku kręcono tu film „ Test pilota Pirxa '' , w 1985 roku „ Lubię nietoperze '' , a w 1990 roku „ Piggate '' .
W Mosznej nakręcono również w 2011 roku sceny do filmu „ Dass '' .
W 2018 roku na terenie pałacu i parku kręcone były sceny do filmu „ Brigitte Bardot cudowna '' .
Upamiętnienie .
23 maja 2022 Narodowy Bank Polski wyemitował w nakładzie do 1 mln sztuk monetę okolicznościową o nominale 5 złotych z serii „ Odkryj Polskę ” – `` Zamek w Mosznej '' .
