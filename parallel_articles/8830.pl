Pałówko
Pałówko ( niem .
`` Neu Paalow '' ) – wieś ulicówka w Polsce położona w województwie zachodniopomorskim , w powiecie sławieńskim , w gminie Postomino .
Znajduje się tu również placówka Ochotniczej Straży Pożarnej Miejscowość położona jest średnio na wysokości 50 m n.p.m .
na wysoczyźnie morenowej płaskiej .
Najwyższe wzniesienie to 53,5 m n.p.m .
W latach 1975–1998 miejscowość administracyjnie należała do województwa słupskiego .
Historia .
Wieś jest osadą , która około 1830 roku powstała zgodnie z planem we wschodniej części obszaru dóbr należących do Pałowa , dlatego nazwa `` Nowe Pałowo '' ( `` Neu Paalow '' ) .
Pani von Below sprzedała całe Pałowo państwu pruskiemu .
W pierwszych dniach marca 1945 roku ( 08.03.1945rok ) wojska radzieckie zajęły wieś .
W 1908 roku powstała we wsi jednoklasowa szkoła .
