Cechy państwa .
Państwo jest organizacją polityczną .
Sensem jego istnienia , osią jego zainteresowania jest rządzenie społeczeństwem .
Jest organizacją globalną , rządzącą całym społeczeństwem .
Państwo jest organizacją przymusową ; ma monopol terytorialny na stanowienie i wykonywanie prawa .
Normy prawne pochodzące od państwa mogą być w razie chęci aparatu państwowego wymuszone fizycznie .
Państwo ma monopol na stosowanie przemocy w celu pozyskania środków na swoje finansowanie – w razie niepłacenia podatków państwo może użyć przemocy fizycznej , aby wymusić ich zapłacenie .
