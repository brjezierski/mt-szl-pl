10 głosi :
`` Państwa są równe pod względem prawnym , mają równe prawa i równą zdolność do korzystania z tych praw oraz mają równe obowiązki .
Prawa każdego państwa nie zależą od jego uprawnień do zapewnienia ich wykonywania , ale od samego faktu jego istnienia jako osoby na mocy prawa międzynarodowego '' .
Kryterium uznania państwa .
Konwencja była regionalną umową międzynarodową i ograniczała się jedynie do państw amerykańskich , ale jej zasady zostały uznane w zwyczajowym prawie międzynarodowym .
Niektórzy wątpią jednak , czy te kryteria są wystarczające .
Podstawowym problemem państwowości jest `` uznanie państwa '' na arenie międzynarodowej , czyli stwierdzenie przez podmiot prawa międzynarodowego faktu istnienia jakiegoś państwa i gotowości do respektowania związanych z tym skutków prawnych .
Przedmiotem uznania może być : państwo , rząd , powstańcy , strona walcząca czy naród .
Uznanie państwa może być udzielone bądź indywidualnie przez podmiot prawa międzynarodowego ( nawiązanie stosunków dyplomatycznych lub podpisanie umowy bilateralnej ) , bądź kolektywnie przez grupę państw ( podobnie jak w przypadku pojedynczego państwa ) lub organizację międzynarodową ( przyjęcie w poczet członków ) .
Uznanie państwa powinno mieć charakter jednoznaczny i nieodwracalny , choć w praktyce spotyka się przypadki zawieszenia lub zerwania stosunków dyplomatycznych czy wykluczenie członka organizacji .
`` Konsekwencje uznania państwa '' : teoria konstytutywna mówi , że państwo istnieje tylko wtedy , gdy jest uznane przez inne państwa , zaś teoria deklaratoryjna twierdzi , że istnienie państwa jest niezależne od uznania go przez inne państwa .
Teorie powstania państwa .
W naukach zajmujących się teorią państwa ( politologia ) nie ma powszechnie uznanej teorii wyjaśniającej problem powstania organizacji państwowej .
W ciągu wieków kształtowały się różnorodne koncepcje dotyczące tego problemu .
