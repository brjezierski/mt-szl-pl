Wólka Panieńska
Wólka Panieńska – wieś w Polsce położona w województwie lubelskim , w powiecie zamojskim , w gminie Zamość .
Do 11 października 1973 w gminie Łabunie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Wieś stanowi sołectwo gminy Zamość .
Historia .
Po raz pierwszy wieś została wzmiankowana w 1780 r. pod nazwą Panieńskie – wraz z Jatutowem określana jako uposażenie klasztoru klarysek w Zamościu .
W następnych dziesięcioleciach nazwa wsi występowała wprawdzie w różnych formach , zawsze jednak uwzględniających byłe właścicielki , czyli „ panny ” klaryski czy szarytki .
Już w 1811 r. wykazana została po raz pierwszy jako Wólka Panieńska , a następnie w roku 1862 i 1893 , jednakże w 1948 r. ponownie użyto nazwy Panieńskie .
Pod rokiem 1911 wspomina się nawet o Kolonii Wólka Panieńska , liczącej zaledwie 6 gospodarstw chłopskich .
W roku 1896 folwark Wólka Panieńska obejmował 148 mórg ziemi uprawnej , w tym 119 mórg gruntów ornych i 22 morgi łąk .
Wedle inwentarza skarbowego z 1925 r. na terenie folwarcznym stały : 2 domy mieszkalne , stajnia , obora , spichlerz , szopa , stodoła i chlewy .
Ponadto w 1928 r. ( wg innych danych w 1895 ) założono tu kaflarnię , dzisiejszą cegielnię .
