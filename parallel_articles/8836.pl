Paniówki
Paniówki ( niem .
`` Klein Paniow '' ) – wieś w Polsce , położona w województwie śląskim , w powiecie gliwickim , w gminie Gierałtowice .
Wedle urzędowego podziału terytorialnego kraju , w obrębie Paniówek wyróżniona jest 1 część miejscowości : Borek .
W latach 1975–1998 miejscowość należała administracyjnie do województwa katowickiego .
Nazwa .
Słownik geograficzny Królestwa Polskiego wydany w latach 1880-1902 podaje dwie polskie nazwy miejscowości `` Paniówek '' oraz `` Paniówki '' , a także niemiecką `` Paniow Klein '' .
Dokument z 1361 roku , wystawiony przez Kazimierza III Wielkiego wymienia `` Paniowki in terra Bythomiensis '' .
Niemiecki nauczyciel i językoznawca amator Heinrich Adamy w swoim dziele o nazwach miejscowości na Śląsku wydanym w 1888 roku we Wrocławiu jako najstarszą nazwę wymienia `` „ Paniow ” '' podając jej znaczenie `` „ Herrschaftlicher Sitz ” '' .
Położenie .
Paniówki sąsiadują z Chudowem , Przyszowicami , Rudą Śląską ( dzielnica Halemba ) , Zabrzem ( dzielnica Kończyce ) i Mikołowem ( dzielnice Borowa Wieś i Paniowy ) .
Znajdują się niemal w centrum Górnośląskiego Okręgu Przemysłowego ( GOP-u ) .
Poprzez drogę krajową nr 44 łączą dwa największe miasta regionu Katowice z Gliwicami oraz Knurowem .
Historia .
.
Później została założona gmina Paniówki .
Paniówki znalazły się w granicach królestwa Czech .
Od 1763 po trzech wojnach śląskich pomiędzy Prusami i Austrią znalazły się w granicach Królestwa Prus , a następnie po zjednoczeniu Prus z Niemcami w 1871 w granicach Niemiec .
W 1910 roku w miejscowości mieszkało 1096 mieszkańców z czego 1061 mówiło po polsku , 26 po polsku i niemiecku , a 9 w języku niemieckim .
W wyborach komunalnych z listopada 1919 roku wszystkie głosy oddano na listy polskie , które uzyskały wszystkie 12 mandatów .
Po podziale Górnego Śląska w 1922 r. Paniówki znalazły się w granicach Polski , w województwie śląskim .
Obecnie należą do w gminy Gierałtowice w województwie śląskim .
W okresie międzywojennym w miejscowości stacjonował komisariat Straży Granicznej i placówka II linii SG „ Paniówki ” .
Parafia i kościół pw .
św .
Urbana w Paniówkach .
Parafia Paniówki istnieje od 1936 roku .
Zanim powstała , wierni uczęszczali do kościoła w Paniowach , będącego kościołem filialnym parafii w Bujakowie .
Uroczyste poświęcenie kościoła nastąpiło 26 listopada 1936 roku , zaś 28 grudnia 1945 roku została powołana kuracja Paniówki , do której przyłączono również sąsiednią parafię Chudów .
2 grudnia 1951 roku oddano do użytku plebanię , a w 1990 roku dom pogrzebowy .
Edukacja .
W Paniówkach działa Publiczna Szkoła Podstawowa .
Inne .
Oprócz szkół w Paniówkach znajduje się także świetlica środowiskowa , w której odbywają się zajęcia dla dzieci i młodzieży , m.in .
nauka gry na gitarze klasycznej , Klub Malucha , pomoc w nauce i wiele innych .
Świetlica znajduje się w niedawno wyremontowanym budynku starej szkoły , w którym znajduje się również filia Gminnej Biblioteki Publicznej , ośrodek zdrowia i biuro sołtysa .
