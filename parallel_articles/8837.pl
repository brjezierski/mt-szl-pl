Październik
Październik jest na półkuli północnej miesiącem jesiennym , a na południowej wiosennym .
Jest najdłuższym miesiącem w roku kalendarzowym , ponieważ wypada wtedy zmiana z czasu letniego na zimowy ( jest dłuższy o jedną godzinę od pozostałych 31-dniowych miesięcy ) .
Nazwa miesiąca ( dawniej również `` paździerzec '' ) pochodzi według Brücknera od słowa `` paździerze '' , oznaczającego „ odpadki od lnu lub konopi ” .
Obok tego funkcjonowały również nazwy : `` paździerzec '' , `` paździerzeń '' , `` pościernik '' , a także `` winnik '' ( por .
starogermańska nazwa miesiąca `` Weinmond '' ) .
Łacińska nazwa `` October '' ( „ ósmy miesiąc ” ; zobacz : kalendarz rzymski ) została zapożyczona przez większość języków europejskich .
W pierwszą niedzielę października w Kościołach mariawickich obchodzona jest uroczystość Matki Bożej Różańcowej .
2 października jest wspomnienie liturgiczne Świętych Aniołów Stróżów .
4 i 10 października mają za patronów świętych Franciszków : Franciszka z Asyżu i Franciszka Borgiasza .
Na wsi dawniej mówiono : `` Po świętym Franciszku chodzi bydło po owsisku '' , co mogło oznaczać zmniejszanie się ilości rosnącej na polu zielonej paszy dla bydła .
7 października jest świętem Matki Bożej Różańcowej .
8 października ( patronka św .
Brygida ) często bywały ciepłe słoneczne dni ze snującym się w powietrzu babim latem .
Mówiono : `` O świętej Brygidzie babie lato przyjdzie '' .
