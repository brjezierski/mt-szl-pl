Peine
Peine – miasto powiatowe nad rzeką Fuhse w środkowych Niemczech , w kraju związkowym Dolna Saksonia , w pobliżu Hanoweru .
Siedziba powiatu Peine .
Liczba mieszkańców wynosi 49 188 ( 2008 ) .
Gospodarka .
Miejscowość jest ośrodkiem przemysłu metalowego , produkcji nawozów fosforowych , nafty i hutnictwa żelaza .
