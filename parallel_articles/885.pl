AS Cannes
Association Sportive de Cannes Football ( w skrócie AS Cannes ) – francuski klub piłkarski , założony w 1909 roku , mający siedzibę w mieście Cannes .
Historia .
Klub powstał w 1902 roku , ale sekcję piłkarską założono dopiero w 1909 roku .
Przez dużą liczbę sezonów AS Cannes grał w Ligue 1 , obecnie gra w Championnat National ( odpowiednik 3. ligi ) .
W AS Cannes swoją karierę piłkarską zaczynał Zinédine Zidane .
Grał tu latach 1988-1992 , po czym jako dwudziestolatek za 3,5 miliona franków i czterech graczy został sprzedany do Girondins Bordeaux .
