Peltaster fructicola
Peltaster fructicola Eric M. Johnson , T.B .
Sutton & amp ; Hodges – gatunek grzybów z klasy `` Dothideomycetes '' .
Jest jednym z patogenów wywołujących brudną plamistość jabłek .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Peltaster , Incertae sedis , Incertae sedis , Incertae sedis , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowany został w roku 1996 na jabłoni domowej w Karolinie Północnej w USA .
Znana jest tylko jego anamorfa , teleomorfa należy do klasy `` Dothideomycetes '' .
Nie określono bliżej jej taksonomii .
Morfologia .
Grzyb mikroskopijny .
Tworzy cienko rozpostarte i zwarte kolonie o oliwkowo-szarej barwie z masami zarodników powstającymi w środkowej części komórek grzybni .
Pojedyncza kolonia hodowana w ciemności w temperaturze 25° C po 21 dniach osiąga średnicę 13–16 mm .
Obrzeża kolonii postrzępione .
Strzępki gładkie z przegrodami , rozgałęzione , początkowo bezbarwne , potem w miarę rozwoju stopniowo ciemniejące do jasnobrązowej barwy .
Mają szerokość 1,5–2 μm .
Konidioforów brak .
Komórki konidiotwórcze monoblastyczne , bezbarwne , powstające bezpośrednio na krótkich wyrostkach strzępek .
Konidia powstają pojedynczo , bazypetalnie na pojedynczych punktach konidiotwórczych .
Są jednokomórkowe , bezbarwne , o kształcie od elipsoidalnego do owalnego i rozmiarach 4–6,5 ( 7 ) × 1,5–2,5 μm .
Konidia te zazwyczaj przez pączkowanie wytwarzają nowe konidia .
Na skórce jabłek tworzy matowe , brązowe , spłaszczone , okrągławe lub nieregularnego kształtu sklerocja o średnicy 68–45 μm .
Na skórce głogu powstają podobne sklerocja , ale o rozmiarach 62–223 μm .
Wytwarza także ciemnobrunatne , okrągłe pyknotyria , w których powstają konidia podobne do tych tworzonych na strzępkach .
Uwalniają się z pyknotyriów przez pęknięcia jego wieczka tworzące się po dojrzeniu konidiów .
