Charakterystyka .
`` Penicillium aurantiogriseum '' to patogen roślinny infekujący szparagi i truskawki .
Wyizolowano z niego związki chemiczne o nazwie anicequol i aurantyna .
Jest pospolity , często izolowany z pożywienia , z warzyw i owoców .
Jest szkodliwy dla ludzi , wytwarza bowiem mykotoksyny , które mogą się dostawać do organizmu wraz z pożywieniem lub przez wdychanie zarodników .
Wytwarzane przez niego mykotoksyny mają działanie rakotwórcze .
Ale ma również pożyteczne działanie .
Jest uznawany za obfite źródło biologicznie aktywnych metabolitów wtórnych , które można stosować przeciwnowotworowo .
Prowadzone są badania nad ich wykorzystaniem w medycynie .
W Polsce stwierdzono występowanie tego gatunku w glebie lasów iglastych na włośnikach i korzeniach drzew i sadzonek , oraz w glebie pod starymi budynkami .
