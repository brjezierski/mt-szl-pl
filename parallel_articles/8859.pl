Występowanie i znaczenie .
`` Penicillium brevicompactum '' powszechnie występuje w powietrzu , w pomieszczeniach i w szerokiej gamie produktów spożywczych .
Wytwarza mykotoksynę o nazwie kwas mykofenolowy ( MPA ) .
Ma ona praktyczne zastosowanie i `` P. brevicompactum '' jest używany do jej wytwarzania .
W badaniach wykazano , że szczep wyizolowany z owocnika błyskoporka podkorowego ( `` Inonotus obliquus '' ) może być użyty do wydajniejszej i bezpieczniejszej produkcji MPA .
`` P. brevicompactum '' jest także jednym z gatunków wytwarzających mewastatynę .
W Polsce wyizolowany został w słodzie , w piasku , glebie i na korzeniach i włośnikach drzew .
