Morfologia i fizjologia .
Hodowla na różnych podłożach jest jedną z metod identyfikacji gatunków rodzaju `` Penicillium '' .
Średnica kolonii `` P. citrinum '' po 7 dniach na różnych podłożach : CYA 27–33 mm , CYA30° C 27–40 mm ; CYA ( 37° C ) 2–12 mm ; MEA 18–25 mm , TAK 29–37 mm , CYAS 29–36 mm , agar kreatynowy 10–19 mm , słaby wzrost , brak lub słaba produkcja kwasu .
