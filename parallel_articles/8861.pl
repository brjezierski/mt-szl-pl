Penicillium commune
Penicillium commune Thom – gatunek grzybów z rodziny `` Aspergillaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy
Morfologia i fizjologia .
Anamorfa `` P. comune '' to grzyb mikroskopijny tworzący na powierzchni nalot potocznie zwany pleśnią .
Z tworzących go strzępek wyrastają prostopadle do nich konidiofory , które na szczycie rozwidlają się wielokrotnie .
Mają zwykle długość od 200 do 400 μm i szorstką powierzchnię .
Konidia mają średnicę od 3,5 do 5,0 μm , są kuliste , gładkie , matowe , szaro-zielone lub szaro-turkusowe i powstają w nieuporządkowanych łańcuszkach bazypetalnych .
Teleomorfa nie jest znana .
Hodowla na różnych podłożach jest jedną z metod identyfikacji gatunków rodzaju `` Penicillium '' .
