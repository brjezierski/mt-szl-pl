Penicillium digitatum
Penicillium digitatum ( Pers . )
Sacc .
– gatunek grzybów z klasy `` Eurotiomycetes '' .
Wraz z `` Penicillium italicum '' wywołuje niebieską zgniliznę owoców cytrusowych , która powszechnie występuje w przechowalniach tych owoców , również w Polsce .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Monilia digitata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Pier Andrea Saccardo w 1881 r .
Synonimy :
Morfologia .
Są to grzyby mikroskopijne tworzące na powierzchni nalot potocznie zwany pleśnią .
