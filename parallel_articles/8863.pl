Penicillium expansum
Penicillium expansum Link – gatunek grzybów z rodziny `` Aspergillaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy
Po raz pierwszy wyizolowany został na jabłku .
Morfologia .
Na agarze ( podłoże Czapeka ) kolonia rozwija się szybko , po 8–10 dniach osiągając średnicę 4–5 cm w temperaturze pokojowej .
Na powierzchni tworzą się promieniste zmarszczki o wysokości 0,5-2 mm .
Konidiofory tworzą się bardzo obficie i regularnie na całej powierzchni kolonii .
W niektórych szczepach konidiofory wyrastają pojedynczo , w innych tworzą wiązki .
Powierzchnia kolonii mączysta lub granulowana , jej obrzeże ma szerokość 1–2 mm .
