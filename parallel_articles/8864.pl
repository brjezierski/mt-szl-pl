Talaromyces funiculosus
Talaromyces funiculosus ( Thom ) Samson , N. Yilmaz , Frisvad & amp ; Seifert ) – gatunek grzybów z rodziny `` Aspergillaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tabalomyces , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1910 r. Charles Thom nadając mu nazwę `` Penicillium funiculosum '' .
W 2010 roku mykolodzy Samson , N. Yilmaz , Frisvad & amp ; Seifert na podstawie badań mikroskopowych i analizy DMA uznali , że nie należy on do rodzaju `` Penicillium '' i przenieśli go do rodzaju `` Talaromyces '' .
Morfologia i fizjologia .
Konidiofory biwerticilioidalne , gładkie o wymiarach 15–100 × 2–3,5 μm ; gałęzie 8–18 μm .
Metule w liczbie od trzech do sześciu , rozbieżne , o wymiarach 6,5–11 × 2–4 μm .
Fialidy ostro zakończone o wymiarach 7,5–11 x 1,5–2,5 μm .
Na jednej metuli jest 3-8 fialid .
Konidia gładkie , elipsoidalne o wymiarach 2–3 ( –5,5 ) × 1–2 ( –2,5 ) μm .
Wykazuje szybki wzrost na ogólnych pożywkach .
Na pożywce CREA w temperaturze 37 oC wytwarza silny kwas sekalonowy .
Teleomorfa nie jest znana .
Występowanie i znaczenie .
W Polsce znajdywany był w glebie na włośnikach , korzeniach , nasionach i w resztkach wielu gatunków roślin ( jodła pospolita , jawor , owies , pomidor , sosna czarna , świerk pospolity , ziemniak , pszenica ) .
Wywołuje chorobę ananasów i jest jednym z gatunków wywołujących peniciliozę cebul .
