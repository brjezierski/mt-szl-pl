Penicillium glabrum
Penicillium glabrum ( Wehmer ) Westling – gatunek grzybów z klasy `` Eurotiomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1893 r. Carl Wehmer nadając mu nazwę `` Citromyces glaber '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Richard Westling w 1911 r .
