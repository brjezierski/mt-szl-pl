Charakterystyka .
`` Penicillium glabrum '' to grzyb mikroskopijny zbudowany ze strzępek , na których wyrastają prostopadle do nich konidiofory na szczycie rozwidlające się wielokrotnie , tak , że swoim wyglądem przypominają pędzel .
Na szczytowych komórkach konidiotwórczych powstają zarodniki konidialne tworzące łańcuszki bazypetalne .
Kolonia `` P. glabrum '' tworzy watowaty nalot popularnie nazywany pleśnią .
Opisano około 300 gatunków `` Penicillium '' .
Rozróżniane są poprzez mikroskopową analizę ich morfologii , analizę metodami biologii molekularnej i analizę budowy DNA .
Najbardziej podobne do `` P. glabrum '' gatunki to `` Penicillium spinulosum '' , `` Penicillium purpurescens '' i `` Penicillium montanense '' .
Sugerowano , że może to być ten sam gatunek .
Badania wykonane na 35 izolatach tych gatunków wykazały jednak , że są to odrębne gatunki .
Modele statystyczne oparte na danych morfologicznych i ogólnych danych fizjologicznych wskazały , że pięć cech miało znaczenie dla rozróżnienia tych gatunków : tekstura ścian konidiów , średnice kolonii na podłożu Czapek i 25 % agarze z azotanem glicerolu , szerokość fialid i średnica pęcherzyka .
`` P. glabrum '' na owocach cytrusowych wytwarza mykotoksynę o nazwie cytromycetyna .
