Penicillium italicum
Penicillium italicum Wehmer – gatunek grzybów z klasy `` Eurotiomycetes '' .
Wraz z `` Penicillium digitatum '' wywołuje niebieską zgniliznę owoców cytrusowych , która powszechnie występuje w przechowalniach tych owoców , również w Polsce .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy wyizolowany został na Sycylii na pomarańczy gorzkiej ( `` Citrus aurantium '' ) w 1894 r .
Synonimy :
Charakterystyka .
W 25 °C kolonie `` P. italicum '' na agarze Czapek rosną w ograniczonym stopniu , ale szybciej na podłożach MEA i PDA .
Kolonie są płaskie , silnie zarodnikujące , o zabarwieniu niebieskim lub szaro-zielonym i często wydają się ziarniste z powodu obecności wiązek konidioforów i główek konidiów .
Odwrotna strona jest bezbarwna lub szara do żółtobrązowej , ale na podłożach takich jak agar z ekstraktem drożdżowym Czapek Dox + ( CYA ) lub sacharoza z ekstraktem drożdżowym barwa może zmienić się w brązowawo-pomarańczową lub czerwonobrązową .
Tekstura jest aksamitna do pofałdowanej , skorupiasta , z nieobecnymi lub bardzo ograniczonymi wydzielinami .
