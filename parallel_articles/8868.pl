Charakterystyka .
`` Penicillium montanense '' charakteryzuje się wysokimi , nierozgałęzionymi lub luźno rozgałęzionymi , bezbarwnymi konidioforami , zakończonymi monowertyklatowymi metulami tworzącymi kuliste , grubościenne konidia o ciemnych ścianach w kolumnach lub splątanych łańcuchach .
Kolonie na agarze z ekstraktem słodowym ( MEA ) osiągają szerokość 4,6–5,4 cm .
W Polsce stwierdzono występowanie tego gatunku na korzeniach brzozy brodawkowatej .
