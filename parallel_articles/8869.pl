Penicillium ochrochloron
Penicillium ochrochloron Biourge – gatunek grzybów z rodziny `` Aspergillaceae '' .
Jest jednym z wielu gatunków `` Penicillium '' ( pędzlaków ) wywołujących chorobę o nazwie penicilioza cebul ( zgnilizna pędzlakowa ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
