Aabenraa
Aabenraa ( Åbenrå , niem .
`` Apenrade '' , fryz .
`` Apenrua '' ) – miasto w Danii położone na Półwyspie Jutlandzkim .
W 2017 roku zamieszkane przez 16153 osoby .
Jest siedzibą gminy Aabenraa w regionie Dania Południowa .
Miasto położone jest 30 km na północ od Flensburga , nad fiordem o tej samej nazwie we wschodniej części duńskiego Szlezwiku .
Wśród mieszkańców Aabenraa znaczący odsetek stanowi mniejszość niemiecka .
Ważny ośrodek kulturalny mniejszości niemieckiej w Danii ( m.in .
gazeta „ Der Nordschleswiger ” , gimnazjum niemieckie ) .
Historia .
Aabenraa otrzymało prawa miejskie w 1240 ( według innych danych – w 1335 ) .
