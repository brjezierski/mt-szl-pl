Penicillium purpurescens
Penicillium purpurescens ( Sopp ) Biourge – gatunek grzybów z klasy `` Eurotiomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1912 r. Olav Johan Sopp nadając mu nazwę `` Citromyces purpurescens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Philibert Melchior Joseph Ehi Biourge w 1923 r .
Morfologia i rozwój .
Kolonie hodowane na agarze Czapka osiągają średnicę 3,0 do 3,5 cm w ciągu 12 do 14 dni w temperaturze pokojowej .
U niektórych szczepów kolonie są wyraźnie podzielone na strefy , u innych prawie bezstrefowe , promieniście pomarszczone .
Składają się z dość grubej , ściśle utkanej grzybni .
Obszar , w którym wytwarzane są konidia składa się z grubych strzępek .
W niektórych szczepach kolonia silnie zarodnikuje na całej powierzchni , w innych tylko na obszarach brzeżnych .
