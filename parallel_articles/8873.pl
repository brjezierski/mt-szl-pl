Penicillium spinulosum
Penicillium spinulosum Thom – gatunek grzybów z klasy `` Eurotiomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Penicillium , Aspergillaceae , Eurotiales , Eurotiomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonim :
Charakterystyka .
`` Penicillium spinulosum '' to grzyb mikroskopijny zbudowany ze strzępek , na których wyrastają prostopadle do nich konidiofory na szczycie rozwidlające się wielokrotnie , tak , że swoim wyglądem przypominają pędzel .
Na szczytowych komórkach konidiotwórczych powstają zarodniki konidialne tworzące łańcuszki bazypetalne .
Konidia są cienkościenne , kuliste , mają powierzchnię kolczastą lub nieregularnie szorstką i powstają w luźnych łańcuszkach .
Średnica konidium waha się od 3,0 do 3,5 µm .
Metule mają zwykle długość od 100 do 300 µm , czasami mogą być krótsze .
Konidiofory `` P. spinulosum '' mogą powstawać z zanurzonych lub powietrznych strzępek .
