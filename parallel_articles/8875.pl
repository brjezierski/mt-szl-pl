– gatunek grzybów z rodziny powłocznikowatych ( `` Peniophoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Peniophora , Peniophoraceae , Russulales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungii '' .
Po raz pierwszy takson ten opisał w 1892 r. Giacomo Bresàdola , nadając mu nazwę `` Corticium aurantiacum '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1994 r. Franz von Höhnel i Viktor Litschauer .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Rozpostarty , przeważnie całkowicie przyrośnięty do podłoża , dopiero starsze owocniki nieco luźniejsze .
Owocnik początkowo o okrągłym kształcie , omijający przetchlinki i inne dziury w zewnętrznej korze drewna , potem pokrywający całą dolną powierzchnię gałęzi , chropowaty , o grubości 0,2–0,5 mm , na grudkach grubszy .
Powierzchnia jasno lub ciemno pomarańczowo-czerwona w zależności od wieku .
Konsystencja młodych owocników mięsista , starszych błoniasta .
Brzeg jaśniejszy , bladożółty lub biały , u młodych okazów włókienkowaty .
System strzępkowy monomityczny .
