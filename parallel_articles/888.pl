Akwizgran
Akwizgran ( niem .
, ; ; ) – miasto uzdrowiskowe na prawach powiatu w Niemczech , w kraju związkowym Nadrenia Północna-Westfalia , w rejencji Kolonia , siedziba regionu miejskiego Akwizgran .
Leży bezpośrednio przy granicy z Belgią i Holandią .
Miasto jest ośrodkiem przemysłu elektrotechnicznego , metalowego , maszynowego , kolejowego i spożywczego ( produkcja czekolady ) oraz stanowi węzeł kolejowy i drogowy .
Znajduje się tu największa politechnika w Niemczech RWTH Aachen licząca ponad 47 000 studentów , wyższa szkoła zawodowa FH Aachen licząca ponad 15 000 studentów , licząca 1 200 studentów siedziba katolickiej uczelni Katholische Hochschule Nordrhein-Westfalen oraz liczne mniejsze uczelnie .
Historia .
W pobliżu Akwizgranu odkryto prehistoryczne , potwierdzone pracami archeologicznymi , celtyckie początki tego miasta .
Zbudowane za czasów rzymskich ( I wiek n.e . )
pod nazwą `` Aquae Grani '' lub `` Aquisgranum '' ( od Granusa , celtyckiego bożka uzdrowień ) jako miejscowość uzdrowiskowa z ciepłymi źródłami siarkowymi ( do 74 °C ) .
Pepin Mały cenił lecznicze właściwości term Granusa w mieście , tam też na miejscu rzymskiej świątyni zbudował kaplicę , a także rezydencję ( palatium ) .
Właśnie od niego pochodzi pierwsza wzmianka o Akwizgranie w roku 765 .
Syn i następca Pepina , Karol Wielki , władca imperium Karolingów , wybrał Akwizgran za swoją siedzibę .
W roku 790 powstał pałac karoliński , oparty na wzorach klasycznych , ze słynną , zachowaną do dziś oktagonalną kaplicą ( w ramach katedry ) .
Budową kaplicy zajął się mistrz Odo z Metzu .
