Przemyślany
Przemyślany ( ukr .
Перемишляни/ '' Peremyszlany '' ) – miasto na Ukrainie , nad Gniłą Lipą , w obwodzie lwowskim , siedziba rejonu przemyślańskiego , 46 km na południowy wschód od Lwowa , do 1945 miasto powiatowe w województwie tarnopolskim , siedziba powiatu przemyślańskiego II Rzeczypospolitej .
Historia .
Pierwsza wzmianka o Przemyślanach jako wsi pochodzi z 1437 .
W 1623 otrzymały prawo magdeburskie .
Do 1772 w województwie ruskim Korony w ramach Rzeczypospolitej , w latach 1772-1918 pod zaborem austriackim w kraju koronnym Galicji i Lodomerii .
Od 1 listopada 1918 do maja 1919 pod administracją Zachodnioukraińskiej Republiki Ludowej , od maja 1919 do 15 marca 1923 pod tymczasową administracją Polski , zatwierdzoną przez paryską konferencję pokojową 26 czerwca 1919 .
Suwerenność Polski nad terytorium Galicji Wschodniej , Rada Ambasadorów uznała 15 marca 1923 .
