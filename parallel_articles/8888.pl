Perpignan
Perpignan ( katal .
`` Perpinyà '' , oksyt .
`` Perpinhan '' , kastyl .
`` Perpiñán '' ) – miasto i gmina w południowej Francji ( Oksytania ) , na wybrzeżu Morza Śródziemnego , ośrodek administracyjny departamentu Pireneje Wschodnie , główne miasto historycznej krainy Roussillon , dawna stolica Królestwa Majorki .
Według danych na rok 2004 gminę zamieszkiwały 116 700 osoby , a gęstość zaludnienia wynosiła 1715 osób/km² ( wśród 1545 gmin Langwedocji-Roussillon Perpignan plasuje się na trzecim miejscu pod względem liczby ludności , natomiast pod względem powierzchni na miejscu dwudziestym pierwszym ) .
W tym samym roku zespół miejski liczył 139 tys .
ludzi .
W mieście znajduje się stacja kolejowa Gare de Perpignan .
17 lipca 1793 roku pod Perpignan rozegrała się bitwa pomiędzy wojskami hiszpańskimi pod dowództwem Antoniego Ricardosa a oddziałami Republiki Francuskiej , dowodzonymi przez Louis-Charles ’ a de Flersa .
Bitwa okazała się zwycięska dla strony francuskiej .
Przyszedł tutaj na świat biatlonista Martin Fourcade wielokrotny mistrz świata i olimpijski .
Klimat .
Średnia dzienna temperatura najwyższa jest w lipcu ( 23,8 °C ) , natomiast najniższa w styczniu ( 8,4 °C ) .
Liczba dni z opadami podobna jest przez cały rok i waha się od 2 do 6 dni w ciągu miesiąca .
