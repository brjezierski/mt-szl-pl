Persepolis FC
Persepolis Football Club ( ) – irański klub piłkarski z siedzibą w Teheranie .
Jest wydzieloną sekcją wielosportowego Persepolis Athletic and Cultural Club .
Klub został założony w grudniu 1963 , natomiast sekcja piłkarska powstała w 1968 .
Obecnie uczestniczy w rozgrywkach Iran Pro League .
Persepolis jest najbardziej utytułowanym klubem w Iranie .
Jest ośmiokrotnym mistrzem kraju , siedem razy rozgrywki ligowe kończył na drugiej pozycji .
Swoje mecze domowe Persepolis rozgrywa na stadionie Azadi , który mieści 100 000 kibiców .
Historia .
Chronologia nazw :
Persepolis został założony w roku 1963 przez boksera Alego Abdeha .
Pierwsze mistrzostwo kraju zespół zdobył dziesięć lat później , w sezonie 1973/1974 .
Od tego czasu jeszcze siedmiokrotnie wygrywał rozgrywki ligowe .
W roku 1991 klub wygrał Azjatycki Puchar Zdobywców Pucharów .
