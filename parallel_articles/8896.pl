Perugia
Perugia , starożytna `` Peruzja '' – stolica Umbrii , rejonu administracyjnego środkowych Włoch .
Miasto leży między Jeziorem Trazymeńskim a Tybrem , liczy około mieszkańców .
Historia .
Peruzja była jednym z dwunastu miast ligi etruskiej .
Pierwsze wzmianki o mieście pochodzą z okresu wojny między Etruskami a Republiką Rzymską w 310 p.n.e .
