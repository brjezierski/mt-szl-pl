Pesaro ( stacja kolejowa )
Pesaro ( wł .
`` Stazione di Pesaro '' ) – stacja kolejowa w Pesaro , w prowincji Pesaro i Urbino , w regionie Marche , we Włoszech .
Znajduje się na linii Bolonia – Ankona .
Według klasyfikacji Rete Ferroviaria Italiana ma kategorią `` złotą '' i jest zarządzana przez Centostazioni .
