Aarhus ( meteoryt )
Aarhus – meteoryt kamienny należący do chondrytów oliwinowo-bronzytowych typu H6 , który spadł 2 października 1951 roku o godzinie 18:15 w duńskiej miejscowości Aarhus .
Przed upadkiem meteoryt rozpadł się na dwie części znane jako Aarhus I ( 300g ) oraz Aarhus II ( 420g ) .
Aarhus I po zderzeniu z ziemią rozpadł się na kolejne cztery odłamki , które odnaleziono w lesie Riis Skov kilka minut po upadku .
Klasyfikacja i skład .
Aarhus to kamienny meteoryt , chondryt oliwinowo-bronzytowy typu petrologicznego H6 o składzie mineralogicznym , w którym przeważają pirokseny ( 40,31 % ) , oliwiny ( 26,47 % ) , plagioklazy ( 6,66 % ) i stop żelazowo-niklowy ( 19,23 % ) .
W mniejszej ilości w składzie zawiera się także chromit ( 0,63 % ) oraz melilit ( 0,41 % ) .
