Petach Tikwa
Petach Tikwa ( hebr .
פֶּתַח תִּקְוָה , `` '' ; arab .
بيتح تكفا ; pol .
dosł .
`` Otwierając Nadzieję '' ) – miasto położone w Dystrykcie Centralnym w Izraelu .
Jest po Hajfie największym ośrodkiem przemysłowym Izraela .
Leży na równinie Szaron w aglomeracji miejskiej Gusz Dan , w otoczeniu miast Bene Berak , Tel Awiw , Ramat ha-Szaron , Hod ha-Szaron , Rosz ha-Ajin , Kirjat Ono i Giwat Szemu ’ el , miasteczka Ganne Tikwa , moszawów Newe Jarak , Kefar Sirkin , Mazor , Nechalim , Magszimim i Kefar Ma ’ as , oraz kibucu Giwat ha-Szelosza .
Na wschód od miasta znajduje się baza wojskowa Sił Obronnych Izraela z dawnym lotniskiem Kefar Sirkin .
Historia .
W 1878 grupa religijnych pionierów z Jerozolimy podjęła inicjatywę założenia pierwszej nowożytnej żydowskiej osady rolniczej w Palestynie .
Pomysłodawcami byli rabini Joel-Mosze Salomon , Dawid Gutmann i Jeszua Stampfer .
Pionierzy planowali zakupić ziemię w dolinie Akor , położonej w pobliżu miasta Jerycho .
Nazwa nowej osady miała nawiązywać do biblijnego wersetu z Księgi Ozeasza 2:17 .
Okazało się jednak , że władze tureckie zablokowały sprzedaż ziemi Żydom .
