Pétanque
Pétanque ( wym .
[ ] ) ( petanka , gra w bule , gra w kule ) – tradycyjna francuska gra towarzyska z elementami zręcznościowymi .
Popularna w szczególności w południowej Francji ( Prowansja ) , ale uprawiana także w innych regionach Francji i w innych krajach europejskich ( Belgia , Hiszpania , Włochy , Szwajcaria , Niemcy , Szwecja ) i na świecie ( Tunezja , Maroko , Wietnam , Tajlandia , Madagaskar , Kanada , Senegal ) .
Rozgrywana jest jako sport nieolimpijski w ramach World Games .
Należy do najliczniej reprezentowanych dyscyplin sportu na świecie – w samej Francji liczba licencjonowanych zawodników wynosi 600 tysięcy .
Petanka jest uprawiana coraz częściej w Polsce , głównie w parkach i niewybetonowanych skwerach .
W Polskiej Federacji Petanque zrzeszonych jest ponad 35 klubów .
Zasady gry .
Rozgrywki odbywają się na pozbawionym trawy placu ( zwanym bulodromem ) , np .
na utwardzonym żwirze , o wystarczających wymiarach ( które dla gry turniejowej wynoszą min .
15×4 m ) .
Gra polega na rzucaniu z wytyczonego okręgu metalowymi kulami ( `` bulami '' ; fr .
`` boule '' – kula ) o średnicy od 70,5 do 80 mm ( ciężar od 650 do 800 g ) w kierunku małej drewnianej lub plastikowej kulki o średnicy 30 mm nazywanej po francusku `` cochonnet '' , co znaczy `` prosiaczek '' ( w Polsce częściej używa się nazwy `` świnka '' ) .
Partię rozgrywa się w jednym z trzech możliwych składów :
Każda partia składa się z kilku rozgrywek .
Rozgrywka – to okres od wyrzucenia świnki do wyrzucenia przez graczy ostatniej kuli .
W danej rozgrywce każdy gracz stara się umieścić swoją bulę bliżej świnki lub wybić punktujące ( stojące najbliżej świnki ) bule przeciwnika .
Rzuca zawsze zawodnik drużyny , której kula aktualnie nie punktuje ( nie znajduje się najbliżej świnki ) .
Po wyrzuceniu wszystkich kul gracze podliczają punkty : zdobywa je tylko zwycięzca rozgrywki ( którego kula stoi najbliżej świnki ) , który otrzymuje punkt za każdą bulę umieszczoną bliżej świnki niż najbliższa śwince bula przeciwnika .
Najniższy możliwy wynik rozgrywki to 1 pkt .
( bądź 0 w przypadku remisu ) , najwyższy w przypadku gry drużynowej 6 pkt. , przy singlach – 3 pkt .
Zwycięzca rysuje nowe kółko w miejscu , gdzie była świnka , po czym rozpoczyna następną rozgrywkę .
Partię wygrywa osoba bądź zespół , który osiągnął jako pierwszy 13 punktów .
Bule .
Istnieje kilka firm produkujących bule ( najpopularniejsze to : Obut , JB , MS Pétanque , Intégrale , La Boule Bleue , La Boule Noire ) , oferujących pełną gamę amatorskich bądź profesjonalnych autoryzowanych zestawów .
Te ostatnie sprzedawane są po trzy identyczne sztuki i ich cena waha się od ok. 180 do 800 , a nawet 1000 złotych za komplet .
Oprócz tego można też kupić amatorskie zestawy produkowane w Chinach , ich cena to ok. 40 zł za zestaw 6 kul .
Wszystkie kule profesjonalne są cechowane .
To znaczy , że posiadają one wygrawerowaną nazwę producenta lub modelu , masę ( w gramach ) , oraz cechę , identyfikującą bule .
Najczęściej spotyka się kule z cechami w postaci „ litera , liczba dwucyfrowa ” ( np .
`` D83 '' , `` X69 '' ) .
Producenci bul oferują także grawerowanie dowolnego napisu , bądź wzoru na kulach .
Zwykle usługa taka wiąże się z dodatkowymi kosztami , choć w niektórych przypadkach jest ona wliczona w cenę zestawu .
Rozmiar buli dobiera się do wielkości dłoni rzucającego .
Inne parametry ( wagę , twardość stali , wykończenie powierzchni kuli ) – do terenu oraz stylu gry i przyzwyczajenia rzucającego .
Właściwości i przydatność zmiennych parametrów bul są tematem nieustających debat i dyskusji wśród graczy .
Puenterzy z reguły wybierają kule twarde , ciężkie i małe , posiadające nacięcia , zaś wybijacze – bule miękkie , lekkie i duże , bez nacięć .
Niemniej występują odstępstwa od tych zasad .
Historia .
Pétanque , grywana mniej więcej od roku 1907 , wywodzi się z innej , podobnej gry , o nazwie `` jeu provençal '' ( „ gra prowansalska ” ) , a jej nazwa – ze słów prowansalskich `` ped tanco '' ( fr .
`` pieds tanqués '' : „ stopy razem ” ) , ponieważ podczas rzutu obie stopy gracza muszą znajdować się wewnątrz wcześniej nakreślonego na placu niewielkiego koła .
Petanka podobnie jak bowls i bocce wywodzi się z uprawianej przez rzymskich legionistów gry w kule , którzy rozpowszechnili ją w całym basenie Morza Śródziemnego .
Pierwsze bule były robione z drewna i obijane zewsząd gwoździami .
W 1930 roku przyjęto definitywnie użycie buli ze stali .
W 1958 roku , w Marsylii powstała Międzynarodowa Federacja Pétanque i Gry Prowansalskiej .
Pierwsze rozgrywki międzynarodowe w tę grę odbyły się w 1959 , a w mistrzostwach świata w 2004 w Grenoble uczestniczyły już 53 kraje , zaś międzynarodowa federacja pétanque zrzeszała 600 tysięcy graczy w 59 krajach .
Polski związek – Polska Federacja Pétanque powstała w 2002 .
Jej siedziba znajduje się we Wrocławiu .
Odmianą gry w kule jest dyscyplina paraolimpijska o nazwie boccia .
2 czerwca 2007 na całym świecie trwały obchody stulecia pétanque .
