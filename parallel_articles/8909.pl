Petricz
Petricz ( ) – miasto w Bułgarii , w obwodzie Błagojewgrad , siedziba administracyjna gminy Petricz .
Według danych szacunkowych Ujednoliconego Systemu Ewidencji Ludności oraz Usług Administracyjnych dla Ludności , 15 czerwca 2020 roku miejscowość liczyła 30 058 mieszkańców .
Historia .
Tereny Petricza zostały włączone do państwa bułgarskiego w 837 roku w wyniku wojny bułgarskiego chana Presjana przeciwko Cesarstwu Bizantyjskiemu .
W późnośredniowiecznym okresie XII–XIV wieku Petricz stał się silną twierdzą – częścią systemu fortyfikacyjnego południowo-zachodniej Bułgarii .
Świadczą o tym pozostałości średniowiecznej fortecy Gjaur kalesi , wokół której pierwotnie powstało miasto .
Przynajmniej w XIII–XIV wieku miasto składało się z dwóch części – twierdzy , czyli tzw .
