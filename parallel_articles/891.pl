Aba ( władczyni )
Aba – w drugiej połowie I wieku p.n.e .
władczyni cylickiego miasta Olba .
Córka tyrana Cylicji Zenofanesa .
Poświadczona przez Strabona [ XIV 5.10 ] , według którego rządy nad Olbą powierzyli jej Marek Antoniusz i Kleopatra .
Następnie straciła władzę , ale rządy pozostały w rękach jej rodziny .
