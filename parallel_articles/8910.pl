Petro Murianka
Petro Murianka , właściwie Piotr Trochanowski ( ur .
10 sierpnia 1947 roku w Parchowie ) – łemkowski poeta .
Życiorys .
W latach 1967–1983 był diakiem w sanockiej cerkwi , a następnie w Krynicy .
Wydał 4 tomiki wierszy – pierwszy w języku łemkowskim , dwa późniejsze w wersji dwujęzycznej i czwarty po polsku – `` Murianczysko '' ( 1983 ) , `` Suchy badyl '' ( 1984 ) , `` Jak sokół wodę z kamienia '' ( 1989 ) oraz wydany w 2002 roku tomik `` Płanetnyky '' .
Pisze również sztuki teatralne ( m.in .
`` Młyńsky kameni '' ) .
Tłumaczy z języków słowiańskich na rusiński i komponuje melodie do wierszy własnych i innych poetów rusińskich .
Obecnie mieszka w Krynicy-Zdroju , gdzie wydaje pierwszą po zmianie ustrojowej gazetę łemkowską „ Besida ” .
Otrzymał m.in .
Nagrodę im .
Stanisława Piętaka ( 1990 ) , statuetkę „ łemkowskiego/rusińskiego niedźwiedzia ” na III Biennale kultury łemkowskiej/rusińskiej w Krynicy Zdroju ( 2004 ) .
Był zastępcą prezesa Stowarzyszenia Łemków , obecnie jako członek tej organizacji .
Wraz z Władysławem Grabanem i reprezentacyjnym zespołem pieśni i tańca „ Łemkowyna ” jest założycielem i współorganizatorem pierwszych Łemkowskich Watr w górach ( 1983 , 1984 – Czarna , 1985 – Hańczowa , 1986–1989 – Bartne ) ; obecnie Łemkowskich Watr na Obczyźnie w Michałowie ( województwo dolnośląskie ) .
Był również członkiem i solistą „ Łemkowyny ” z Bielanki .
Śpiewał także w zespole Media „ Żurawli ” .
Został odznaczony Srebrnym ( 2013 ) i Złotym ( 2022 ) Krzyżem Zasługi .
