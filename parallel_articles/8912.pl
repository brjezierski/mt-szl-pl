Petrowiriwka
Petrowiriwka ( ukr .
`` Петровірівка '' , hist. ``
Żowteń '' ) – wieś na Ukrainie w rejonie berezowskim obwodu odeskiego .
Nazwa .
Od 1927 do 2016 roku miejscowość nazywała się „ Żowteń ” ( ukr .
`` Жовтень '' ) .
Dzieje .
Wieś założona została w 1814 na terytorium , które w 1792 przyłączono do Imperium Rosyjskiego po traktacie w Jassach .
W 1830 włączono Petrowiriwkę w granice tyraspolskiego ujezdu guberni chersońskiej .
W 1927 władza komunistyczna zmieniła nazwę wioski na „ Żowteń ” ( ukr .
`` Жовтень '' ) .
Nazwa ta była używana do 2016 roku .
Obecnie wieś liczy 2954 mieszkańców i jest jedną z największych wsi rejonu .
We wsi urodził się akademik RAN T.I .
