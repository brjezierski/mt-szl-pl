Petrykozy ( województwo zachodniopomorskie )
Petrykozy ( niem .
`` Althof '' ) – wieś w Polsce położona w województwie zachodniopomorskim , w powiecie kołobrzeskim , w gminie Rymań .
Miejscowość wchodzi w skład sołectwa Jarkowo .
Według danych z 4 września 2013 r. Petrykozy miały 115 mieszkańców .
