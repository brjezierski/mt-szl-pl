Abakan ( instalacja )
Abakan – rodzaj tkaniny artystycznej będącej monumentalną kompozycją przestrzenną zbliżoną do rzeźby , autorstwa Magdaleny Abakanowicz .
Tkanina w dziełach Abakanowicz posiada urozmaiconą fakturę , artystka wprowadziła relief , ażur , elementy przestrzenne , a jako tworzywo sizal .
Tkaniny te określane są czasem mianem „ tkanych rzeźb ” .
Cykl takich kompozycji , tworzonych z różnych rodzajów włókien ( m.in .
sizalu , końskiego włosia , sznurów rozmaitej grubości ) i wysoce ekspresyjnych w charakterze , przyniósł Abakanowicz w roku 1965 główną nagrodę na biennale w São Paulo .
Wczesne abakany prezentowano na wystawie w londyńskiej Tate Modern w lutym 2023 .
