Liternica nadrzewna
Liternica nadrzewna ( `` Phaeographis dendritica '' ( Ach . )
Müll .
Arg . )
– gatunek grzybów z rodziny literakowatych ( `` Graphidaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Graphidaceae , Ostropales , Ostropomycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1803 r. Erik Acharius nadając mu nazwę `` Opegrapha dendritica '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1882 r. Johannes Müller Argoviensis .
