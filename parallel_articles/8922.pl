Ciemnotwardnik bocznotrzonowy
Ciemnotwardnik bocznotrzonowy ( `` Phaeomarasmius rimulincola '' ( Rabenh . )
Scherff . )
– gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Tubariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go Gottlob Ludwig Rabenhorst w 1851 r. , nadając mu nazwę `` Agaricus rimulincola '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Aladár Scherffel w 1914 r .
