Phallus indusiatus
Phallus indusiatus – gatunek grzybów z rodziny sromotnikowatych ( `` Phallaceae '' ) .
W Chinach określany jest jako `` zhu sheng '' ( 竹笙 , pinyin : zhúshēng ) lub `` zhu sun '' ( 竹荪 ; pinyin : zhúsūn ) .
Występowanie .
Gatunek spotykany jest w Meksyku , Ameryce Południowej , Malezji , południowych Chinach i Japonii .
Jest saprotroficzny .
W Chinach spotykany jest w zagajnikach bambusowych .
