Czyreń jodłowy
Czyreń jodłowy ( `` Phellinus hartigii '' ( Allesch .
& amp ; Schnabl ) Pat . )
– gatunek grzybów z rodziny szczeciniakowatych ( `` Hymenochaetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phellinus , Hymenochaetaceae , Hymenochaetales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1890 r. Allescher i Schnabl nadając mu nazwę `` Polyporus hartigii '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1903 r. Patouillard , przenosząc go do rodzaju `` Phellinus '' .
Synonimy naukowe :
Nazwę polską nadali Barbara Gumińska i Władysław Wojewoda w 1983 r. W polskim piśmiennictwie mykologicznym gatunek ten opisywany był też jako czyreń Hartiga .
Morfologia .
Szerokość 5-20 cm , grubość 5-20 cm .
Młode okazy mają bulwiasty kształt , który z czasem zmienia się na konsolowaty lub kapeluszowaty .
Zewnętrzna powierzchnia jest gładka , falista , koncentrycznie strefowana .
Ma kolor od szarego do ciemnobrązowego , brzeg ( krawędź przyrostu ) jest jaśniejszy ; w kolorze od białawego do ochrowobrązowego .
