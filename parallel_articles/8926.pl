Guzoczyrka rokitnikowa
Guzoczyrka rokitnikowa ( czyreń rokitnikowy ( `` Fomitiporia hippophaëicola '' ( H. Jahn ) Fiasson & amp ; Niemelä ) ) – gatunek grzybów z rodziny szczeciniakowatych ( `` Hymenochaetaceae '' ) .
Wywołuje chorobę zwaną zgorzelą pnia .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Fomitiporia , Hymenochaetaceae , Hymenochaetales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1889 r. Hermann Jahn nadając mu nazwę `` Phellinus hippophaeicola '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1984 r. J.-L.F. Fiasson i Tuomo Niemelä , przenosząc go do rodzaju `` Fomitiporia '' .
Synonimy :
Nazwę polską czyreń rokitnikowy nadali Barbara Gumińska i Władysław Wojewoda w 1983 r. Wówczas gatunek ten zaliczany był do rodzaju `` Phellinus '' ( czyreń ) .
Według Index Fungorum gatunek ten obecnie należy do rodzaju `` Fomitiporia '' , tak więc nazwa polska jest niespójna z aktualną nazwą naukową .
Komisja ds .
Polskiego Nazewnictwa Grzybów Polskiego Towarzystwa Mykologicznego zarekomendowała w 2021 r. używanie nazwy guzoczyrka rokitnikowa .
