Rdzawoporka kosmata , czyreń kosmaty ( `` Fuscoporia torulosa '' ( Pers . )
T. Wagner & amp ; M .
Fisch . )
– gatunek grzybów z rodziny szczeciniakowatych ( `` Hymenochaetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Fuscoporia , Hymenochaetaceae , Hymenochaetales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1818 r. Christiaan Hendrik Persoon , nadając mu nazwę `` Boletus torulosus '' .
Potem zaliczany był do różnych rodzajów .
Obecną nazwę , uznaną przez Index Fungorum , nadali mu T. Wagner i M. Fisch .
w 2001 r .
