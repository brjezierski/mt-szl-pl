Korkoząb ciemny
Korkoząb ciemny ( `` Phellodon melaleucus '' ( Sw. ex Fr . )
P .
Karst . )
– gatunek grzybów należący do rodziny chropiatkowatych ( `` Thelephoraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phellodon , Thelephoraceae , Thelephorales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowali w 1815 roku Olof Swartz i Elias Fries nadając mu nazwę `` Hydnum melaleucum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1881 roku Petter Adolf Karsten , przenosząc go do rodzaju `` Phellodon '' .
Niektóre synonimy : .
Nazwę polską podali Barbara Gumińska i Władysław Wojewoda w 1983 r. W checklist W. Wojewody z 2003 jest opisany pod błędną nazwą `` Phellodon connatus '' ( według Index Fungorum `` P. connatus '' to synonim `` Phellodon tomentosus '' ) .
