Phialophora cinerescens
Phialophora cinerescens ( Wollenw . )
J.F.H .
Beyma – gatunek grzybów z rodziny `` Herpotrichiellaceae '' .
U goździków ( `` Dianthus '' ) powoduje chorobę o nazwie fialoforoza goździka .
Jest organizmem kwarantannowym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phialophora , Herpotrichiellaceae , Chaetothyriales , Chaetothyriomycetidae , Eurotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Takson ten utworzył w 1930 r. Hans Wilhelm Wollenweber nadając mu nazwę `` Verticillium cinerescens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu J.F.H .
Beyma w 1940 r .
Rozwój .
`` Phialophora cinerescens '' może przetrwać w glebie saprotroficznie przez wiele lat .
W szklarni w Holandii po tym , jak wystąpiła w niej fialoforoza goździka , zaprzestano uprawy tej rośliny , uprawiano natomiast inne .
Gdy po 13 latach znów wprowadzono goździki , zostały one zaatakowane przez `` P. cinerescens '' .
