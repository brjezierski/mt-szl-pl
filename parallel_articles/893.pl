Abancay
Abancay – miasto w południowym Peru , w Andach Środkowych , w dolinie rzeki Pachachaca , stolica prowincji Abancay .
Położone jest na wysokości 2400 m n.p.m .
Jest stolicą regionu Apurímac .
Leży na wschodnim brzegu rzeki Marino .
Nazwa miasta pochodzi od słowa `` amankay '' , które w języku Indian Keczua oznacza dziko rosnący kwiat , podobny do białej lilii .
