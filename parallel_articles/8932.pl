Johannes Reinelt
Johannes Reinelt , znany pod pseudonimem Philo vom Walde po polsku `` Filozof z lasu '' ( ur .
5 sierpnia 1858 r. w Gołuszowicach , zm .
16 stycznia 1906 r. we Wrocławiu ) – był niemieckim pisarzem , poetą i nauczycielem , ale przede wszystkim był jednym z najwybitniejszych twórców poezji w dialekcie śląskim II polowy XIX w. na Górnym Śląsku .
