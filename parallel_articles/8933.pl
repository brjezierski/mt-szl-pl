Phnom Penh
Phnom Penh ( , trl .
`` Phnum Pénh '' ) – stolica Kambodży położona w południowo-zachodniej części kraju , przy ujściu rzeki Tonle Sap do Mekongu .
1,5 mln mieszkańców ( 2012 ) .
Główny ośrodek przemysłowy i naukowo-kulturowy kraju .
Zakłady przemysłu spożywczego ( łuszczarnie ryżu , browary , cukrownie , olejarnie , przetwórnie ryb , przemysł tytoniowy ) , drzewnego , włókienniczego , metalowego i elektromaszynowego ( budowa łodzi , montaż ciągników , przemysł samochodowy ) .
Liczne rzemiosło artystyczne ( wyroby ze srebra i kości słoniowej ) .
Duży port rzeczny dostępny dla małych statków morskich .
Międzynarodowy port lotniczy w ( Pochentong ) .
