Nazwę polską nadał Franciszek Błoński w 1890 .
W polskim piśmiennictwie mykologicznym gatunek ten ma też nazwy łuszczak złotawy i łuszczak złotorunny .
Morfologia .
Średnica 5–12 cm .
U młodych osobników jest stożkowy , później półkulisty , a w końcu płaski .
