Łuskwiak złotawy
Łuskwiak złotawy ( `` Pholiota aurivella '' ( Batsch ) P. Kumm ) – gatunek grzybów należący do rodziny pierścieniakowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Pholiota , Strophariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w roku 1786 August Johann Georg Karl Batsch nadając mu nazwę `` Agaricus aurivellus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w roku 1871 Paul Kummer przenosząc go do rodzaju `` Pholiota '' .
