Didymella pinodella
Didymella pinodella ( L.K .
Jones ) Q. Chen & amp ; L. Cai – gatunek grzybów z klasy `` Dothideomycetes '' .
Saprotrof i pasożyt .
U grochu jest jednym z patogenów wywołujących chorobę o nazwie zgorzelowa plamistość grochu .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Didymella , Didymellaceae , Pleosporales , Pleosporomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1927 r. L.K .
Jones nadając mu nazwę `` Ascochyta pinodella '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Q. Chen & amp ; L. Cai w 2015 r .
Synonimy :
Morfologia .
Grzyb mikroskopijny .
Kolonie na pożywce agarowo-owsianej są nieco szorstkie , szaro-brązowe , stopniowo czerniejące .
Tworzą obficie niemal kuliste pyknidia o średnicy 200–300 μm .
Komórkami konidiotwórczymi są hialinowe , krótkie fialidy powstające z komórek wyścielających wnętrze pyknidiów .
Powstają w nich enteroblastycznie głównie bezprzegrodowe , czasami tylko z jedną przegrodą konidia o rozmiarach 4,5–8 ( -10 ) × 2–3 μm .
Są zazwyczaj hialinowe , ale w starszych koloniach czasami żółtoczerwone .
Mogą tworzyć się także ciemnobrązowe chlamydospory o kształcie kulistym lub nieregularnym i gładkich ścianach .
Powstają one pojedynczo , lub w łańcuchach , zarówno na końcu strzępek , jak i w ich środkowej części .
Teleomorfę obserwowano jedynie w laboratorium w hodowli `` in vitro '' .
Wytwarza ona pseudotecja o wymiarach 140–250 μm .
Zawierają one worki o długości 140–290 μm i szerokości 20–30 μm .
Powstają w nich askospory o wymiarach 25–35 × 12,5–19 μm .
Plamy na liściach grochu wytwarzane przez `` Didymella pinodella '' są wieloboczne , drobne i mają barwę od ciemnobrunatnej do czarnej .
Odróżniają się tym od plam tworzonych przez 2 pozostałe patogeny wywołujące zgorzelową plamistość grochu ( `` Didymella pisi '' i `` Didymella pinodes '' ) .
