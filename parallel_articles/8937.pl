Phomopsis abietina
Phomopsis abietina ( R. Hartig ) Grove – gatunek grzybów z klasy `` Sordariomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phomopsis , Diaporthaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1889 r. Robert Hartig nadając mu nazwę `` Phoma abietina '' .
Obecną nazwę nadał mu William Bywater Grove w 1918 r .
Znaczenie .
Grzyb mikroskopijny pasożytujący na jodle pospolitej ( `` Abies alba '' ) i wywołujący u niej chorobę o nazwie przewężenie i zamieranie pędów jodły .
Znany jest tylko w postaci anamorfy .
Tworzy zagłębione w tkankach jodły podkładki , a w nich wydłużone , owalne konidia o wymiarach 10–15 × 2–6 µm .
Znajduje się na liście organizmów patogennych dla roślin w kategorii II – mikroorganizmy , o których wiadomo , że wywołują epidemie wśród roślin uprawnych oraz ozdobnych .
