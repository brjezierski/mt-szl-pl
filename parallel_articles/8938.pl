Diaporthe leptostromiformis
Diaporthe leptostromiformis ( J.G .
Kühn ) Rossman & amp ; Udayanga – gatunek grzybów z rzędu `` Diaporthales '' .
U łubinu wywołuje grzybową chorobę o nazwie brunatna plamistość łodyg łubinu .
Wytwarza też mykotoksyny powodujące u zwierząt karmionych porażonym łubinem chorobę wątroby zwaną lupinozą .
Systematyka .
`` Diaporthe , Diaporthaceae , Diaporthales , Diaporthomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungii '' .
Po raz pierwszy gatunek ten opisał Julius Gotthelf Kühn w 1880 r. nadając mu nazwę `` Cryptosporium leptostromiforme '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Rossman i Udayanga w 2015 r .
Synonimy :
Morfologia i rozwój .
Grzyb mikroskopijny , saprotrof i pasożyt roślin .
Na łodygach porażonych roślin anamorfa patogenu tworzy podkładki rzekome ( pseudostromy ) o barwie od szarobrunatnej do czarnej , długości 0,3–2 mm , szerokości 0,2–1 mm i grubości 0,1–3 mm .
Początkowo znajdują się pod skórką , potem skórka nad nimi ulega złuszczeniu .
Wewnątrz są białe , zbudowane z bardzo grubościennych strzępek , z zewnątrz pokryte grubościenną plektenchymą , co upodabnia je do sklerocjów .
