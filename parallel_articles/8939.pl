Phomopsis prunorum
Phomopsis prunorum ( Cooke ) Grove – gatunek grzybów z rzędu `` Diaporthales '' .
U niektórych drzew owocowych powoduje chorobę o nazwie zgorzel pędów drzew owocowych .
Charakterystyka .
Grzyb mikroskopijny .
Kultury `` Phomopsis prunorum '' na PDA w 22 °C w ciemności rosną szybko .
