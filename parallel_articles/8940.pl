Phragmidium bulbosum
Phragmidium bulbosum ( Fr . )
Schltdl .
– gatunek grzybów z rodziny `` Phragmidiaceae '' .
Grzyb mikroskopijny , pasożyt .
Na jeżynach wywołuje chorobę o nazwie rdza jeżyny .
Czasami występuje także na różach , gdzie wraz z gatunkami `` Phragmidium mucronatum '' i `` Phragmidium tuberculatum '' ( główni sprawcy tej choroby ) wywołuje chorobę o nazwie rdza róży .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1832 r. Elias Fries nadając mu nazwę `` Aregma bulbosum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1824 r. Schltdl .
