Phragmidium mucronatum
Phragmidium mucronatum ( Pers . )
Schltdl .
– gatunek grzybów z rodziny `` Phragmidiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1794 r. Christian Hendrik Persoon nadając mu nazwę `` Puccinia mucronata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1824 r. Diederich Franz Leonhard von Schlechtendal .
Synonimów na około 50 .
Morfologia .
Ecja pomarańczowe , pylące .
Powstają na dolnej stronie liści , na pędach i na owocach .
