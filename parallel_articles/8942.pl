Phragmidium potentillae
Phragmidium potentillae ( Pers . )
P. Karst .
– gatunek grzybów z rodziny `` Phragmidiaceae '' .
Grzyb mikroskopijny porażający rośliny z rodzaju pięciornik ( `` Potentilla '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Persoon nadając mu nazwę `` Puccinia potentillae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1878 r. Petter Karsten , przenosząc go do rodzaju `` Phragmidium '' .
