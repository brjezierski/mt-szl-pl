Phragmidium rosae-pimpinellifoliae
Phragmidium rosae-pimpinellifoliae ( Rabenh . )
Dietel – gatunek grzybów z rodziny `` Phragmidiaceae '' .
Grzyb mikroskopijny pasożytujący na róży gęstokolczastej ( `` Rosa spinosissima '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1873 r. Gottlob Ludwig Rabenhorst nadając mu nazwę `` Phragmidium rosarum '' f. `` rosae-pimpinellifoliae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1880 r. Paul Dietel .
Morfologia i rozwój .
Pyknidia rzadko spotykane , prawdopodobnie dlatego , że są niszczone przez rozwijające się ecja .
Występują w rozproszonych grupach pod skórką rośliny , mają nieokreślony kształt i wysokość 15-30 µm .
Ecja pojawiają się na łodygach , czasami także na ogonkach liściowych , głównych nerwach liściowych i owocach .
