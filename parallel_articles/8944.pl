Phragmidium rubi-idaei
Phragmidium rubi-idaei ( DC . )
P. Karst .
– gatunek grzybów z rodziny `` Phragmidiaceae '' .
Grzyb mikroskopijny pasożytujący na malinie właściwej ( `` Rubus idaeus '' ) .
Wywołuje u niej chorobę o nazwie rdza maliny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1815 r. de Candolle nadając mu nazwę `` Puccinia rubi-idaei '' .
