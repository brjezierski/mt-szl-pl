Phragmidium sanguisorbae
Phragmidium sanguisorbae ( DC . )
J. Schröt .
– gatunek grzybów z rodziny `` Phragmidiaceae '' .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach roślin z rodziny różowatych .
Występuje także w Polsce .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1815 r. Augustin Pyramus de Candolle , nadając mu nazwę `` Puccinia sanguisorbae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1887 r. Joseph Schröter .
Charakterystyka .
Miodowej barwy , okrągławe spermogonia powstają w grupach na liściach w obrębie fioletowych plam .
