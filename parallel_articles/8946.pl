Phragmidium tuberculatum
Phragmidium tuberculatum Jul .
Müll .
– gatunek grzybów z rodziny `` Phragmidiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phragmidium , Phragmidiaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Występowanie i siedlisko .
`` P. tuberculatum '' występuje głównie w Europie , ale podano także stanowiska w Azji i w Australii oraz w Ameryce Północnej ( Alaska , Connecticut i Kanada ) .
W Polsce odnotowano występowanie tego gatunku na `` Rosa agrestis , Rosa canina , Rosa dumalis , Rosa gallica , Rosa glauca , Rosa majalis , Rosa rubiginosa , Rosa rugosa , Rosa tomentosa '' i in .
Pasożyt obligatoryjny , jednodomowy i pełnocyklowy .
Znaczy to , że może się rozwijać tylko na żywych organizmach , jego rozwój odbywa się na jednym tylko żywicielu , i w trakcie tego rozwoju wytwarza wszystkie 5 właściwych dla rdzy rodzajów zarodników .
Pasożytuje na licznych gatunkach róż ( `` Rosa '' ) .
Wraz z pokrewnymi `` Phragmidium mucronatum '' i `` Phragmidium bulbosum '' wywołuje u nich chorobę o nazwie rdza róży .
Poszczególne gatunki i odmiany róż mają różną odporność na tę chorobę .
Gatunki podobne .
`` Phragmidium mucronatum '' i `` Phragmidium tuberculatum '' są trudne do odróżnienia i często mylone są z sobą .
Do wielu informacji o występowaniu tych gatunków należy podchodzić z dozą nieufności , istnieje uzasadnione podejrzenie , że nie zawsze były one prawidłowo rozpoznane .
Teliospory `` P. tuberculatum '' składają się z 4-6 , rzadziej 3 lub 7 komórek i mają ostro zakończony dzióbek o długości do 18 μm .
Obydwa te gatunki różnią się też mikroskopowo budową ecjów i urediniospor , ale nie zawsze są to cechy jednoznaczne do interpretacji .
Obecnie dość łatwo można te gatunki oznaczyć badaniem DNA .
