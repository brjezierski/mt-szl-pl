Phyllachora
Phyllachora Nitschke ex Fuckel – rodzaj grzybów z klasy `` Sordariomycetes '' .
Grzyby mikroskopijne , patogeny roślin .
Charakterystyka .
Do rodzaju `` Phyllachora '' należą bardzo liczne gatunki .
Joanna Marcinkowska w 2012 r. podaje ich liczbę 944 , ale liczba ta zmienia się w wyniku prac mykologów porządkujących taksonomię i ustalających pokrewieństwo gatunków .
Przedstawiciele rodzaju są szeroko na świecie rozprzestrzenione i występują we wszystkich strefach klimatycznych .
Grzyby mikroskopijne , pasożyty roślin .
Żyją na liściach wielu gatunków roślin z rodzin wiechlinowatych ( `` Poaceae '' ) , bobowatych ( `` Fabaceae '' ) , trojeściowatych ( `` Asclepiadaceae '' ) , krasnodrzewowatych ( `` Erythroxylaceae '' ) .
