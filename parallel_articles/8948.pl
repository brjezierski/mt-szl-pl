Phyllachoraceae Theiss .
& amp ; P. Syd .
– rodzina grzybów z klasy `` Sordariomycetes '' .
Charakterystyka .
Są szeroko rozprzestrzenione i występują w różnych strefach klimatycznych .
Liczne gatunki są pasożytami roślin porażającymi ich liście i pędy .
Owocniki typu perytecjum powstają zazwyczaj w skupiskach w pseudostromie zanurzonej w tkance rośliny .
W ujściu owocników znajdują się peryfizy .
