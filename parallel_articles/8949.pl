Phyllachorales
Phyllachorales M.E .
Barr – rząd workowców należący do klasy `` Sordariomycetes '' .
Charakterystyka .
Należące do tego rzędu gatunki występują powszechnie , szczególnie w strefie o klimacie tropikalnym .
Są pasożytami roślin powodującymi powstawanie na powierzchni porażonych liści różnobarwnych plam ( żółtych , czerwonych , brązowych ) .
Joanna Marcinkowska w 2012 r. podała , że rząd liczy 2 rodziny , 63 rodzaje i 1226 gatunków .
Liczby te ulegają jednak ciągłej zmianie w wyniku prac mykologów porządkujących taksonomię i ustalających pokrewieństwo gatunków .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Phyllachorales , Sordariomycetidae , Sordariomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Rząd ten jest zaliczany według kodeksu `` Index Fungorum '' do klasy `` Sordariomycetes '' .
