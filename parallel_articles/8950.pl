Phyllactinia guttata
Phyllactinia guttata ( Wallr . )
Lév .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' .
Pasożyt atakujący wiele gatunków drzew i krzewów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Erysiphe , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon jako `` Sclerotium erysiphe '' ß `` corylea '' .
W 1819 r. Carl Friedrich Wallroth nadał mu nazwę `` Alphitomorpha guttata '' .
