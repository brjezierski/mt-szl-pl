Poroblaszek żółtoczerwony
Poroblaszek żółtoczerwony ( `` Phylloporus rhodoxanthus '' ( Schwein . )
Bres . )
– gatunek grzybów należący do rodziny borowikowatych ( `` Boletaceae '' ) .
Jest jedynym europejskim przedstawicielem borowikowatych o blaszkowatym hymenoforze .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phylloporus , Boletaceae , Boletales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1822 r. Lewis David von Schweinitz nadając mu nazwę `` Agaricus rhodoxanthus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Giacomo Bresàdola w 1900 r .
