Phyllosticta polygonorum
Phyllosticta polygonorum Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt rozwijający się na niektórych gatunkach roślin z rodziny rdestowatych ( `` Polygonaceae '' ) .
Wywołuje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phyllosticta , Phyllostictaceae , Botryosphaeriales , Incertae sedis , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Morfologia .
Endobiont rozwijający się wewnątrz tkanek porażonych roślin .
Konidia owalne lub elipsoidalne , hialinowe , drobne , o rozmiarach 4–5,5 × 2–3 μm , bez śluzowatej osłonki i bez szczytowego wyrostka .
Występowanie .
Roślinami żywicielskimi `` Phyllosticta polygonorum '' w Chinach są m.in .
gryka zwyczajna ( `` Fagopyrum esculentum '' ) , `` Polygonum orientale '' , `` Polygonum platyphyllum '' .
W Polsce notowany na rdeście ostrogorzkim ( `` Persicaria hydropiper '' ) i rdeście plamistym ( `` Polygonum persicaria '' ) .
