Phyllosticta solitaria
Phyllosticta solitaria Ellis & amp ; Everh – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożyt niektórych roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phyllosticta , Phyllostictaceae , Botryosphaeriales , Incertae sedis , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1895 r. Job Bicknell Ellis i Benjamin Matlack Everhart na liściach gruszy `` Pyrus coronaria '' .
Synonim : `` Phyllostictina solitaria '' ( Ellis & amp ; Everh . )
Shear 1923 :
Morfologia .
Teleomorfa nie jest znana , ale wiosną na opadłych liściach opisano struktury podobne do niedojrzałych owocników .
Spermogonium nie jest znane .
Anamorfa tworzy pyknidia o różnej budowie .
W obrębie plam na liściach są one drobne , cienkościenne , kuliste , o wymiarach 60–95 μm i mają wystający dziób z ostiolą 9–12 × 7–12 μm .
Pyknidia na ogonkach liściowych mają rozmiar 62–119 μm , a ostiola 12–14 × 9–12 μm .
Na owocach pyknidia są elipsoidalne , spłaszczone , o wymiarach 57–95 × 107–166 μm , z ujściem 12–23 μm i są grubościenne ( ściana przy podstawie ma grubość 4,75 μm ) .
