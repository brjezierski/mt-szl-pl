Piacenza ( stacja kolejowa )
Piacenza ( wł : `` Stazione di Piacenza '' ) – stacja kolejowa w Piacenzy , w regionie Emilia-Romania , w prowincji Piacenza , we Włoszech .
Znajduje się na linii Mediolan-Bolonia , Alessandria-Piacenza , Piacenza-Cremona .
Stacja została otwarta w 1859 .
Jest zarządzana przez Centostazioni i Rete Ferroviaria Italiana .
Rocznie z usług stacji korzysta około 5 mln pasażerów .
