Abazyni
Abazyni , Abazowie , Abazyjczycy , Abazyńczycy ( abaz .
абаза , `` abaza '' ; ros .
абазины , `` abaziny '' ) – rdzenna ludność ( grupa etniczna ) północnego Kaukazu , blisko spokrewniona z Czerkiesami .
Zamieszkują północne stoki Kaukazu , głównie w Karaczajo-Czerkiesji ( ok. 27,5 tys .
osób ) , a także w niewielkich grupach w Adygei w okolicach Kisłowodzka .
Około 60 % to ludność wiejska .
Ogólną liczbę Abazynów zamieszkujących Federację Rosyjską ocenia się na ok. 33 tys .
osób ( dane ze spisu w 1989 ) .
Niewielkie grupy zamieszkują także Turcję , Syrię , Jordanię i Liban ( w sumie około 11 tysięcy osób ) , dokąd wyemigrowali w połowie XIX wieku , w okresie wojen kaukaskich ( 1817–1864 ) .
Abazyni posługują się językiem abazyńskim , należącym do grupy abchasko-adygejskiej języków kaukaskich .
Pod względem etnograficznym Abazyni dzielą się na dwie grupy : `` Tapanta '' i `` Szkaraua '' .
Wyznają przede wszystkim islam sunnicki .
