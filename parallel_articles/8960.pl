Pichilemu
Pichilemu ( w języku mapodungun `` Mały Las '' ) – turystyczne miasto i gmina w środkowej części Chile .
Jest stolicą Prowincji Cardenal Caro .
Geografia .
Pichilemu leży 126 km na zachód od San Fernando i bezpośrednio graniczy z Oceanem Spokojnym .
Sąsiaduje z gminami Placilla , Nancagua , Chépica , Santa Cruz , Pumanque , Palmilla , Peralillo , Navidad , Lolol , Litueche , La Estrella , Chile , Marchihue i Paredones , jest częścią dystryktu elektoralnego N° 35 i należy do Regionu O'Higgins .
Historia .
Miejscowość została założona 6 października 1845 roku .
Turystyka .
Plaża w Pichilemu jest jednym z najbardziej znanych miejsc do uprawiania surfingu , szczególnie w sektorze Punta de Lobos , gdzie odbywają się często zawody .
Miejscowość jest także częstym miejscem łowienia ryb i agrokultury .
