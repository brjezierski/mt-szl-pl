Podbuż
Podbuż ( ) – osiedle typu miejskiego w rejonie drohobyckim obwodu lwowskiego .
W II Rzeczypospolitej miejscowość była siedzibą gminy wiejskiej Podbuż w powiecie drohobyckim w województwie lwowskim .
W Podbużu urodził się Józef Dietl – polski lekarz , polityk , profesor i rektor Uniwersytetu Jagiellońskiego , prezydent Krakowa w latach 1866–1874 .
