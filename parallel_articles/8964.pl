Piekary Śląskie
Piekary Śląskie ( śl .
`` Piekary '' , hist .
: Piekary Niemieckie , niem .
`` Deutsch Piekar '' , 1939–1945 `` Scharley-Deutsch Piekar '' ) – miasto na prawach powiatu , położone w południowej Polsce , na Górnym Śląsku , w województwie śląskim , w centrum Górnośląskiego Okręgu Przemysłowego ( GOP ) .
Piekary Śląskie wchodzą w skład Górnośląsko-Zagłębiowskiej Metropolii .
Miasto leży nad rzeką Brynicą , w północnej części Wyżyny Śląskiej .
Pierwsza wzmianka o Piekarach pochodzi z 1253 roku .
Pierwsze zapisy nazwy wsi w dokumentach średniowiecznych brzmiały `` Pecare '' , a później `` Peccari theutonicale '' ( 1332 rok ) .
W okresie międzywojennym do 1934 roku obowiązywała nazwa `` Wielkie Piekary '' ( lub `` Piekary Wielkie '' ) ; 1 kwietnia 1934 roku Wielkie Piekary połączono z Szarlejem w gminę `` Szarlej-Wielkie Piekary '' , po czym 3 września 1935 roku nazwę zmieniono gminy na Piekary Śląskie .
W latach 1939–1945 przywrócono podwójną nazwę gminy w niemieckiej wersji językowej – `` Scharley-Deutsch Piekar '' .
Miejscowość posiada ustrój miejski od 1 stycznia 1940 roku , który z powodu okupacji niemieckiej został wprowadzony w życie dopiero dnia 13 lutego 1947 roku z mocą wsteczną od 1 stycznia 1947 roku .
Piekary Śląskie są od XVII wieku ośrodkiem pielgrzymkowym oraz jednym z głównych w Polsce ośrodków kultu maryjnego z obrazem Matki Boskiej Piekarskiej .
Miasto uznawane jest przez wielu za duchową stolicę Górnego Śląska .
Według danych GUS z 30 czerwca 2020 roku , miasto liczyło około 55 tys .
