Pieksämäki
Pieksämäki – miasto i gmina w Finlandii .
Zwarte osadnictwo istniało tu od XIV wieku , region administracyjny ( pitäjä ) utworzono w latach 1574-1575 , prawa miejskie nadano w roku 1962 .
Miasto jest położone w prowincji Finlandia Wschodnia wśród lasów i jezior , jest częścią Południowej Savonii .
Pieksämäki zawdzięcza swój rozwój skrzyżowaniu szlaków kolejowych : kolei savońskiej z Helsinek do Oulu przez Kuopio , której dworzec kolejowy powstał w r. 1889 oraz linii w 3 innych kierunkach , tj .
do Joensuu , Savonlinny ( dochodzącej kiedyś aż do Petersburga ) i Tampere .
Dziś miasto żyje z usług , choć plany wybudowania tu ogromnego centrum handlowego pod nazwą Ideapark nie zostały zrealizowane .
Znaczące budynki : drewniany kościół ( A. Sorsa 1753 ) , wieża ciśnień ( Aarne Ervi 1956 ) , Centrum Kultury Poleeni ( Gullichsen-Kairamo-Vormala 1989 ) .
