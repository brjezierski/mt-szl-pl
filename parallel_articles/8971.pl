Pietas
Pietas – rzymska personifikacja przywiązania , pobożności i sumienności .
Łączona z hellenistycznym podaniem o Caritas Romana , nie była jednak związana z jakimkolwiek rzymskim mitem .
Jej atrybutami były róg obfitości oraz bocian .
Na rewersach monet przedstawiano ją zazwyczaj jako stojącą lub siedzącą matronę , podczas spełniania ofiary libacyjnej nad ołtarzem ; często również wyobrażana w towarzystwie dziecka lub dzieci .
Miała własną świątynię na Forum Holitorium , wzniesioną około 191 p.n.e .
Wraz z formułą PIETAS AVGVSTI/AVGVSTAE przedstawienie na pieniądzu obiegowym miało symbolizować moralne cnoty władcy lub jego małżonki .
