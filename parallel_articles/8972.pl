Pietna
Pietna ( niem .
`` Pietna '' ) – wieś w Polsce położona w województwie opolskim , w powiecie krapkowickim , w gminie Krapkowice , na ziemi prudnickiej .
Od 1950 należy do województwa opolskiego .
Nazwa .
Nazwa wsi wywodzi się prawdopodobnie od słowa podnóże/wzniesienie lub też miejsce gdzie była woda pitna .
Jako , '' Pietna '' '' zostaje wymieniona w 1784 r. W okresie hitlerowskiego reżimu w latach 1936-1945 miejscowość nosiła nazwę `` Teichgrund '' .
Wieś wymieniana była w swej historii także jako `` Piethna '' , `` Pietien '' .
9 września 1947 r. nadano miejscowości , wówczas administracyjnie należącej do powiatu prudnickiego , polską nazwę `` Pietna '' .
