Pietraszyn
Pietraszyn ( dodatkowa nazwa w j. niem .
Klein Peterwitz , ) – wieś w Polsce położona w województwie śląskim , w powiecie raciborskim , w gminie Krzanowice .
Liczy około 350 mieszkańców .
Historycznie leży na Górnym Śląsku , w tzw .
`` polskich Morawach '' , czyli na obszarze dawnej diecezji ołomunieckiej .
Historia .
Nie zachowały się żadne informacje o historii wsi w średniowieczu .
Od 1652 do 1925 wieś należała do niemieckojęzycznej parafii w Sudicach .
Po wojnach śląskich miejscowość znalazła się w granicach Prus .
Od 1818 w powiecie raciborskim .
Według spisu z 1910 była zamieszkała w 96 % przez czeskojęzycznych ( zobacz gwary laskie ) Morawców .
W granicach Polski od końca II wojny światowej .
W latach 1975–1998 miejscowość administracyjnie należała do województwa katowickiego .
