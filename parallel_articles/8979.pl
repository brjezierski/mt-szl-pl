Pikachu
Pikachu ( jap .
ピカチュウ `` Pikachū '' ) – fikcyjna postać z serii Pokémon .
Pikachu jest jednym z najbardziej rozpoznawalnych Pokémonów , głównie ze względu na fakt , że jest on centralną postacią w serii anime .
Pikachu jest powszechnie uważany za najbardziej popularnego Pokémona i jest traktowany jako oficjalna maskotka całej serii Pokémon .
Charakterystyka .
Pikachu jest żółty z brązowymi paskami na plecach , czarno zakończonymi uszami i czerwonymi policzkami .
Do ewoluowania w Raichu potrzeba mu kamienia Gromu .
W drugiej i w kolejnych generacjach Pokémon ten ma także swoją wcześniejszą ewolucję – Pichu , który zmienia się w Pikachu , gdy nawiąże silną więź ze swoim trenerem .
