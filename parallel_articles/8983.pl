Pilobolus lentiger
Pilobolus lentiger Corda – gatunek grzybów z rzędu pleśniakowców ( `` Mucorales '' ) .
Grzyb saprotroficzny i koprofilny żyjący na odchodach zwierząt roślinożernych .
Systematyka .
Nazwa polska grzybów z rodzaju `` Pilobolus '' to zrywka .
Występowanie .
Znane jest występowanie `` Pilobolus lentiger '' tylko w Europie .
W Polsce podano dwa stanowiska ( Adam Wodziczko 1911 , Alina Skirgiełło i Zadara 1979 ) .
