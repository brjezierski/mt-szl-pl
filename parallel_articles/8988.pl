Pingliang
Pingliang ( chiń .
平凉市 ; pinyin : `` Píngliàng Shì '' ) – miasto o statusie prefektury miejskiej w Chinach , w prowincji Gansu .
W 2010 roku liczba mieszkańców miasta wynosiła 111 355 .
Prefektura miejska w 1999 roku liczyła 2 137 213 mieszkańców .
