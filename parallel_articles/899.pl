Język abchaski ( abch . )
– język należący do rodziny języków północno-zachodniokaukaskich , używany przez Abchazów .
Jest używany w Abchazji oraz częściowo w Adżarii i Turcji .
Posługuje się nim ok. 100 tys .
osób .
Z perspektywy typologii ma charakter aglutynacyjny i polisyntetyczny .
Abchaski jest blisko spokrewniony z językiem abazyńskim .
Wyróżnia się dialekty : abżujski ( legł u podstawy języka literackiego ) , bzybski oraz sadzki ( w Turcji ) .
