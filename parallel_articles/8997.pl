Piotr Żyła
Piotr Paweł Żyła ( ur .
16 stycznia 1987 w Cieszynie ) – polski skoczek narciarski , zawodnik klubu WSS Wisła , reprezentant Polski , olimpijczyk z Soczi 2014 , Pjongczang 2018 oraz z Pekinu 2022 .
Zdobywca siedmiu medali mistrzostw świata – dwóch złotych ( 2021 i 2023 ) i brązowego ( 2017 ) w konkursach indywidualnych oraz złotego ( 2017 ) i trzech brązowych ( 2013 , 2015 i 2021 ) w konkursach drużynowych .
Dwukrotny drużynowy brązowy medalista mistrzostw świata w lotach narciarskich ( 2018 i 2020 ) .
Drużynowy wicemistrz świata juniorów z 2005 .
Drugi zawodnik 65 .
Turnieju Czterech Skoczni .
Trzeci zawodnik Letniego Grand Prix 2018 .
W konkursach indywidualnych Pucharu Świata był 22 razy na podium ( w tym dwukrotnie na 1. miejscu ) .
Były rekordzista Polski w długości skoku narciarskiego .
Wielokrotny medalista mistrzostw Polski , w tym pięciokrotny indywidualny mistrz ( dwa tytuły zimą i trzy latem ) .
