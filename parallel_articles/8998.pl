Piptocephalis freseniana
Piptocephalis freseniana de Bary – gatunek grzybów z rzędu zwierzomorkowców ( `` Zoopagales '' ) .
Grzyb mikroskopijny i pasożytniczy .
Jest gatunkiem typowym rodziny `` Piptocephalidaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum :
`` Piptocephalus , Piptocephalidaceae , Zoopagales , Incertae sedis , Zoopagomycetes , Zoopagomycotina , Zoopagomycota , Fungi '' .
Gatunek ten opisał w 1865 r. Anton de Bary na odchodach zwierzęcych w Niemczech .
Synonimy :
Morfologia i tryb życia .
Występuje głównie na odchodach gryzoni , rzadziej w glebie .
Jest pasożytem grzybów z rzędu `` Mucorales '' , z których pobiera pokarm za pomocą ssawek .
Tworzy kilkukrotnie dichotomicznie rozgałęzione sporangiofory .
Na końcowych ich rozgałęzieniach tworzy merosporangia o cylindrycznym kształcie .
Zarodniki o różnym kształcie , dojrzałe są uwalniane w postaci suchej lub w kropli cieczy .
W Polsce był wielokrotnie izolowany na grzybie `` Mucor '' sp .
( pleśniak ) rosnącym na odchodach zwierzęcych .
W laboratorium można go hodować na bogatym w składniki odżywcze podłożu hodowlanym , takim jak agary MEYE lub Emerson ’ s YpSs .
Do hodowli dobrze nadaje się także płynna pożywka z drożdży `` Cokeromyces recurvatus '' .
