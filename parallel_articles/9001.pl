Bandera piratów
Bandera piracka ( ang .
`` Jolly Roger '' pol .
`` Wesoły Roger '' ) – bandera wywieszana na maszcie statku w celu identyfikowania się pomiędzy piratami w XVIII wieku ( późna część Złotego Wieku Piractwa ) .
Bandera powszechnie znana jako „ Jolly Roger ” , przedstawiaja najczęściej czaszkę i piszczele na czarnym tle .
Używana była już w drugiej dekadzie XVIII wieku przez takich piratów jak `` Black Sam '' Bellamy , Edward England , czy John Taylor .
Historia .
Zapoczątkowanie nazwy .
Nazwa „ Jolly Roger ” , w odniesieniu do pirackiej bandery , została użyta po raz pierwszy w książce Charlesa Johnsona pt .
`` Historia najsłynniejszych piratów , ich zbrodnicze wyczynki i rabunki '' ( ang .
`` A General History of the Robberies and Murders of the most notorious Pyrates '' ) , wydrukowanej w Wielkiej Brytanii w 1724 .
Johnson nazwę `` „ Jolly Roger ” '' usłyszał od pirata Bartholomew Roberts w czerwcu 1721 , a następnie od kapitana Francis Spriggs w grudniu 1723 .
Roberts i Spriggs używali tej samej nazwy , choć ich flagi były zupełnie inne , co zasugerowało , że `` „ Jolly Roger ” '' jest potoczną nazwą każdej pirackiej bandery .
Ani flaga Robertsa , ani flaga Spriggsa , nie zawierały kości i piszczeli .
Richard Hawkins , który został porwany przez piratów w 1724 , zanotował , że piraci używają czarnej flagi z postacią szkieletu przebijającego serce dzidą .
