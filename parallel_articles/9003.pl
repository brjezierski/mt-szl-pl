Pirot
Pirot ( cyr .
Пирот ) – miasto w Serbii , stolica okręgu pirockiego i siedziba miasta Pirot .
Leży nad rzeką Niszawą .
W 2011 roku liczyło 38 785 mieszkańców .
W mieście znajduje się węzeł drogowy .
Historia .
Pierwsze wzmianki o mieście pojawiły się w IV wieku , wówczas nosząc nazwę `` Turres '' .
W 1836 roku mieszkańcy miasta wzniecili powstanie przeciwko Turkom , zginęły tysiące ludzi .
Podczas wojny serbsko-bułgarskiej w 1885 roku odbyła się tam jedna z bitew ( przegrana przez Serbów ) .
Pod koniec XIX wieku było tu 13 klasztorów oraz 72 kościoły różnych wyznań .
