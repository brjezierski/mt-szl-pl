Piza
Piza ( ) – miasto we Włoszech , w Toskanii , 85 379 mieszkańców , nad rzeką Arno , oddalone o 13 km od wybrzeża Morza Tyrreńskiego .
Piza jest także jedną z prowincji Toskanii o powierzchni 2448 km² , zamieszkaną przez 384 957 osób , podzieloną na 39 gmin .
Historia .
Założona przez Etrusków lub Ligurów , później port morski .
Stała się potężną republiką między X a XII wiekiem .
W roku 1081 uzyskała prawa miejskie i zrzeczenie się księcia Toskanii wszelkiej jurysdykcji oraz nadanie praw mieszkańcom wyboru margrabiów .
Piza wzbogaciła się w tym okresie na łupach pochodzących z kampanii przeciwko Saracenom na Sycylii i Sardynii – najlepiej świadczy o tym kunszt wykonania słynnej w świecie Krzywej Wieży ( `` Torre Pendente '' ) .
Od XVI w. podupadło głównie z winy nanoszonego przez rzekę Arno mułu , który doprowadził do zapiaszczenia portu .
W 1409 miejsce soboru .
W Pizie znajduje się również jeden ze starszych uniwersytetów na świecie , utworzony w 1343 .
