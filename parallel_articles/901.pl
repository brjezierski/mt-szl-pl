23 maja 2011 roku niepodległość Abchazji uznało również Vanuatu , jednak 20 maja 2013 roku premier tego państwa ogłosił zerwanie wzajemnych stosunków .
Drugim podobnym przypadkiem jest Tuvalu – państwo to uznało Abchazję 18 września 2011 r. , lecz wycofało to uznanie 31 marca 2014 roku .
Ponadto niepodległość Abchazji uznały jeszcze w 2006 roku trzy inne nieuznawane na arenie międzynarodowej republiki – Osetia Południowa , Górski Karabach i Naddniestrze .
Geografia .
Większą część terytorium ( ok. 70 % ) zajmują góry – południowe stoki głównego grzbietu Kaukazu ( ważniejsze przełęcze : Kłuchorska i Maruchska ) oraz jego odgałęzienia : na północnym zachodzie Góry Gagryjskie , w centrum Góry Bzypijskie i Góry Abchaskie , na południowym wschodzie Góry Kodorskie .
Najwyższym szczytem jest Dombaj-Ulgen ( 4046 m n.p.m . ) .
Góry zachodniej Abchazji składają się głównie ze skał węglanowych które rozpuszczają się w wodzie , tworząc jaskinie krasowe .
Najsłynniejszymi z nich są Jaskinia Nowoatońska i Jaskinia Abrskila .
Z kolei w masywie Arbaa znajduje się Jaskinia Krubera – najgłębsza jaskinia na świecie .
W Abchazji znajduje się ponad 180 jezior .
Największymi i najbardziej znanymi są jeziora Amtkjal i Rica .
Abchazja ma niewielkie zasoby węgla kamiennego , rud cynku , ołowiu , miedzi , rtęci i barytu , oraz surowców budowlanych .
Kraj obfituje w liczne źródła mineralne ( np .
miejscowość Awdachara ) .
Główne rzeki : Bzyp , Kodori , Psou , Gumista i Galidzga .
Na wybrzeżu panuje klimat podzwrotnikowy , wilgotny , w górach od 500 do 2000 m n.p.m .
