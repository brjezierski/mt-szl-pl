Separatystyczny rząd Abchazji przyjmuje , że jest ona niepodległym państwem – jednostronna proklamacja niepodległości nastąpiła w 1992 roku .
Podczas wojny domowej w Gruzji władze rosyjskie przyczyniły się w decydującym stopniu do przetrwania niezależnego państwa abchaskiego poprzez wsparcie militarne i finansowe .
Dzisiaj Rosja wciąż posiada szerokie wpływy polityczne i wojskowe we władzach republiki , `` de facto '' będąc gwarantem jej istnienia .
Tuż po jednostronnym proklamowaniu niepodległego państwa przez Kosowo w lutym 2008 roku , prezydent Abchazji zapowiedział , że skieruje wniosek do Rosji i innych państw Wspólnoty Niepodległych Państw o uznanie państwowości republiki .
Władze republiki zainteresowane są wejściem Abchazji do Związku Białorusi i Rosji jako trzeciego członu państwowego , a jako minimum – uzyskaniem statusu niepodległego państwa , luźno stowarzyszonego z Rosją .
W sierpniu 2008 , po wygranej wojnie , w Abchazji odbyły się masowe demonstracje z żądaniem uznania niepodległości Abchazji .
