Progruziński rząd Abchaskiej Republiki Autonomicznej , do czasu wojny separatystów z Gruzją w sierpniu 2008 , rezydował w Czchałcie w Górnej Abchazji .
Współcześnie rząd Abchazji ( a nie rząd Abchaskiej Republiki Autonomicznej ) włada całym terytorium kraju .
