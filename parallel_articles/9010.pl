Pisklaki
Pisklaki – wieś w Polsce położona w województwie lubelskim , w powiecie biłgorajskim , w gminie Łukowa .
Leży nad Tanwią .
W latach 1975–1998 miejscowość administracyjnie należała do województwa zamojskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 248 mieszkańców i była trzecią co do wielkości miejscowością gminy Łukowa .
