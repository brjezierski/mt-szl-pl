Pitești
Pitești ( dawniej nazywane po polsku `` Piteszt '' ) – miasto na Wołoszczyźnie w Rumunii , stolica okręgu Ardżesz , nad rzeką Argeș .
Jest ważnym ośrodkiem handlowym i przemysłowym .
Liczy 168 458 mieszkańców .
W pobliżu Pitești w mieście Mioveni znajduje się fabryka i siedziba marki samochodów Dacia .
W Pitești kończy się istniejący odcinek rumuńskiej autostrady A1 z Bukaresztu .
W 1882 r. urodził się tu Ion Antonescu , związany potem z miastem również w biegu swojej kariery wojskowej , m.in .
w 1933 r. jako dowódca dywizji stacjonującej w mieście .
Na początku lat 50. w tutejszym więzieniu miał miejsce niesławny eksperyment , polegający na `` reedukacji '' więźniów poprzez tortury .
Na przełomie lat 60. i 70. w kooperacji z francuskim Renault pobudowano fabrykę samochodów .
Gospodarka .
W mieście rozwinął się przemysł chemiczny , maszynowy , elektromaszynowy , samochodowy , drzewny , elektrotechniczny , materiałów budowlanych , włókienniczy oraz spożywczy .
Sport .
W mieście tym siedzibę ma klub piłkarski Argeș Pitești .
Z Pitești wywodzą się piłkarze Nicolae Dobrin i Bogdan Stancu .
