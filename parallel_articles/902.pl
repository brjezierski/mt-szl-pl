Abdurrahman Wahid
Abdurrahman Wahid , znany także jako Gus Dur ( ur .
7 września 1940 w Jombang , zm .
30 grudnia 2009 w Dżakarcie ) – prezydent Indonezji w latach 1999 do 2001 , przywódca Partii Przebudzenia Narodowego , którą założył po upadku reżimu Suharto .
Abdurraham Wahid objął urząd prezydenta w wyniku wyborów prezydenckich , które odbyły się w październiku 1999 roku .
W chwili wybrania na urząd prezydenta był postrzegany przez społeczeństwo jako osoba , która poprawi stan gospodarki i zaprowadzi porządek w państwie .
Krótko po wyborach doszło do nieporozumień między Wahidem a parlamentem .
Jednocześnie pod presją społeczeństwa Wahid odsunął od władzy wojskowych odpowiedzialnych za mordy w Timorze .
Prezydentowi zarzucano niezdolność do podejmowania ważnych decyzji ( która doprowadziła do kryzysu finansowego w kraju ) , korupcję oraz niepotrzebne polemiki z różnymi grupami politycznymi .
Wahid popadł w konflikty z elitami politycznymi i wojskowymi , które popierały poprzednie rządy Suharto .
W 2001 roku odszedł ze stanowiska na rzecz Megawati Soekarnoputri .
