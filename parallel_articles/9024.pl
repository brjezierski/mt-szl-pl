Piramida ( Skały Kroczyckie )
Piramida – skała na wzgórzu Łysak na Wyżynie Częstochowskiej .
Znajduje się w obrębie wsi Kroczyce w województwie śląskim , w powiecie zawierciańskim , w gminie Kroczyce .
Zbudowana z wapienia skała znajduje się w lesie i jest najbardziej na wschód wysuniętą skałą na wzgórzu Łysak .
Zaliczana jest do grupy Skał Kroczyckich .
Skała od strony północnej ma kształt piramidy o wysokości kilku metrów .
Na kilku skałach tego wzgórza uprawiana jest wspinaczka skalna .
Na Piramidzie jak dotąd nie poprowadzono dróg wspinaczkowych , ale jest to rejon o nie do końca jeszcze wyeksploatowanych możliwościach i Piramida może potencjalnie stanowić obiekt wspinaczki skalnej lub boulderingowej .
