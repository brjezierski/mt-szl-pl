Wkrótce po ślubie Tasman został zatrudniony przez Holenderską Kompanię Wschodnioindyjską ( hol .
`` Vereenigde Oostindische Compagnie '' ( VOC ) ) , podpisując w 1633 kontrakt na trzy lata .
Do 1634 pływał jako oficer na statku handlowym pomiędzy Batawią a Molukami .
W lipcu 1634 został mianowany szyprem małego statku `` Mocha '' .
W 1637 odwiedził Holandię , by uregulować swoje sprawy i w październiku 1638 powrócił wraz z żoną na stałe do Batawii .
Po upływie pierwszego kontraktu z VOC , podpisał drugi dziesięcioletni .
Tasman brał udział w kolejnych wyprawach organizowanych przez kompanię .
W 1639 wysłano go pod komendą Mathijsa Quasta na wschód celem odkrycia legendarnych `` Wysp Złota i Srebra '' , które miały leżeć na wschód od Japonii .
Wyprawa zakończyła się fiaskiem .
