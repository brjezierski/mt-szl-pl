Piastów ( przystanek kolejowy )
Piastów – przystanek kolejowy na linii podmiejskiej , położony przy ul .
Dworcowej w Piastowie , w województwie mazowieckim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca aglomeracyjnego .
Z przystanku PKP Piastów można dojechać elektrycznymi pociągami podmiejskimi na zachód ( do Skierniewic , Grodziska Mazowieckiego i Żyrardowa ) i na wschód ( do Warszawy Śródmieście , a dalej do Mińska Mazowieckiego , Otwocka , Sulejówka Miłosny ) .
Na przystanku zatrzymują się pociągi podmiejskie różnych relacji Kolei Mazowieckich i linia S1 SKM .
