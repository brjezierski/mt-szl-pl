Piechanin
Piechanin – wieś w Polsce położona w województwie wielkopolskim , w powiecie kościańskim , w gminie Czempiń .
W okresie Wielkiego Księstwa Poznańskiego ( 1815-1848 ) miejscowość wzmiankowana jako Piechanin należała do wsi większych w ówczesnym pruskim powiecie Kosten rejencji poznańskiej .
Piechanin należał do okręgu czempińskiego tego powiatu i stanowił część prywatnego majątku Borówko , którego właścicielem był wówczas Nieczkowski .
Według spisu urzędowego z 1837 roku Piechanin liczył 251 mieszkańców , którzy zamieszkiwali 27 dymów ( domostw ) .
W latach 1975–1998 miejscowość administracyjnie należała do województwa poznańskiego .
