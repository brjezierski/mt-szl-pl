Piekoszów ( przystanek kolejowy )
Piekoszów – przystanek kolejowy w mieście Piekoszów , pomiędzy drogami do Rykoszyna i Łazisk .
Obsługuje lokalny ruch pasażerski do Kielc , Włoszczowy i Częstochowy .
Odprawę przesyłek towarowych umożliwiają znajdujące się w pobliżu dodatkowe tory towarowe , rampa wyładowcza i plac ładunkowy .
W roku 2017 przystanek obsługiwał 0–9 pasażerów na dobę
Niektóre pomieszczenia budynku stacyjnego zaadaptowano na cele mieszkalne .
W pozostałej części budynek niszczeje .
