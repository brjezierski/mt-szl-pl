Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 14 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy gminy Prudnik z gminą Korfantów .
Należy do Euroregionu Pradziad .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
