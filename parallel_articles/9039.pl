Piorunkowice
Piorunkowice ( , , ) – wieś w Polsce położona w województwie opolskim , w powiecie prudnickim , w gminie Prudnik .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
Przepływa przez nią rzeka Ścinawa Niemodlińska .
W latach 1975–1998 miejscowość administracyjnie należała do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 206 osób .
