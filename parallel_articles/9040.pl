Pies domowy
Pies domowy ( `` Canis familiaris '' ) – udomowiony gatunek ssaka drapieżnego z rodziny psowatych ( Canidae ) , traktowany przez niektóre ujęcia systematyczne za podgatunek wilka , a przez inne za odrębny gatunek , opisywany pod synonimicznymi nazwami `` Canis lupus familiaris '' lub `` Canis familiaris '' .
Od czasu jego udomowienia powstało wiele ras , znacznie różniących się morfologią i cechami użytkowymi .
Rasy pierwotne powstawały głównie w wyniku presji środowiskowej .
Rasy współczesne uzyskano w wyniku doboru sztucznego .
Przez ISSG ( Invasive Species Specialist Group ) jest uznawany za gatunek inwazyjny .
Taksonomia .
Karol Linneusz opisał naukowo psa w 1758 r. pod nazwą `` Canis familiaris '' .
W tej samej pracy Linneusz opisał wilka pod nazwą `` Canis lupus '' .
