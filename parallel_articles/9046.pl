We wsi znajduje się pałac , wybudowany został po 1893 roku przez ówczesnego właściciela wsi Hermana von Leipzigera z okazji jego ślubu z hrabianką Eweliną von Rittberg .
W 1901 posiadłość nabył hrabia Ignacy Bniński , prezes Towarzystwa Sztuk Pięknych w Poznaniu , który całkowicie przebudował pałac .
Jego bryła zachowała się do dzisiaj .
Hrabia Ignacy Bniński , wielki miłośnik sztuki , gościł w Pietronkach wielu przedstawicieli świata artystycznego .
Bywali tu m.in .
malarz Julian Fałat , rzeźbiarz Sylwester Mańczak , a kolekcja dzieł sztuki w pałacu była jedną z najciekawszych w Wielkopolsce .
Hrabia Bniński zmarł w 1920 i został pochowany na Cmentarzu w Pietronkach .
Po 1945 pałac popadł w ruinę .
W 1978 został odrestaurowany i obecnie znajduje się tu centrum szkoleniowo konferencyjne .
