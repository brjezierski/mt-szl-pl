Piaski ( rejon brzostowicki )
Piaski ( biał .
Пяскі ; ros .
Пески ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie brzostowickim , w sielsowiecie Olekszyce .
Historia .
Dawniej w województwie trockim Rzeczypospolitej Obojga Narodów .
W czasach zaborów w granicach Imperium Rosyjskiego , w guberni grodzieńskiej .
W latach 1921–1939 ówczesna okolica leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Wielkie Ejsymonty .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 78 osób , 49 było wyznania rzymskokatolickiego , 29 prawosławnego .
