Piętno ( przystanek kolejowy )
Piętno − dawny kolejowy wąskotorowy przystanek osobowy w Piętnie , w Polsce , w województwie wielkopolskim , w powiecie tureckim , w gminie Tuliszków .
Został zbudowany w latach 1914-1917 razem z linią do Turku .
W lipcu 1991 roku został zamknięty dla ruchu pasażerskiego .
Od czerwca 2002 roku jest używany w ruchu towarowym .
Należy do Kaliskiej Kolei Dojazdowej .
Obecnym jej operatorem jest Stowarzyszenie Kolejowych Przewozów Lokalnych
