Pięty
Pięty – wieś w Polsce położona w województwie świętokrzyskim , w powiecie skarżyskim , w gminie Bliżyn .
1 lutego 1944 Niemcy spacyfikowali wieś .
Śmierć poniosło 7 osób .
W latach 1975–1998 miejscowość administracyjnie należała do województwa kieleckiego .
Wierni kościoła rzymskokatolickiego należą do parafii Parafia św .
Rocha w Mroczkowie .
