Liczba pierwsza
Liczba pierwsza – liczba naturalna większa od 1 , która ma dokładnie dwa dzielniki naturalne : jedynkę i siebie samą .
Nie istnieje powszechnie przyjęty symbol zbioru wszystkich liczb pierwszych , czasami oznacza się ten zbiór symbolem formula_1
