Rzepin Pierwszy
Rzepin Pierwszy – wieś w Polsce , położona w województwie świętokrzyskim , w powiecie starachowickim , w gminie Pawłów .
Do 1954 roku istniała gmina Rzepin .
W latach 1975–1998 miejscowość położona była w województwie kieleckim .
Przez miejscowość przebiega droga wojewódzka nr 756 .
Wierni Kościoła rzymskokatolickiego należą do parafii Chrystusa Odkupiciela w Rzepinie .
Historia .
Wieś z rodowodem historycznym sięgającym X wieku , początkowo własność szlachecka komesa Radosława , następnie od roku 1180 klasztoru bożogrobowców w Miechowie .
Od roku 1339 do 1819 własność klasztoru łysogórskiego na Świętym Krzyżu .
W drugiej połowie XIX wieku istniała gmina Rzepin , z urzędem gminy w Radkowicach .
Gminę o powierzchni 12307 mórg , zamieszkiwało wówczas 5102 mieszkańców .
