Wolica Pierwsza
Wolica Pierwsza – wieś w Polsce położona w województwie lubelskim , w powiecie janowskim , w gminie Modliborzyce .
Krótki opis .
W latach 1975–1998 miejscowość administracyjnie należała do województwa tarnobrzeskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 760 mieszkańców i była trzecią co do wielkości miejscowością gminy Modliborzyce .
Miejscowa ludność wyznania katolickiego przynależy do Parafii św .
Stanisława w Modliborzycach .
