Chmiel Pierwszy
Chmiel Pierwszy – wieś w Polsce , położona w województwie lubelskim , w powiecie lubelskim , w gminie Jabłonna .
Leży przy drodze wojewódzkiej nr 836 .
Wieś stanowi sołectwo gminy Jabłonna .
W latach 1975–1998 wieś administracyjnie należała do ówczesnego województwa lubelskiego .
Historia .
Chmiel w roku 1432 „ Chmyel ” , 1459 „ Chmielow ” , 1460 „ Chmyelnik ” historycznie położony w powiecie lubelskim parafii Krzczonów .
Wieś stanowiła własność królewską .
W roku 1432 Boruta herbu Luba występuje w dokumentach jako tenutariusz z Chmiela .
