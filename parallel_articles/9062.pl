Dokudów Pierwszy
Dokudów Pierwszy – wieś ( dawniej miasto ) w Polsce położona w województwie lubelskim , w powiecie bialskim , w gminie Biała Podlaska .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bialskopodlaskiego .
Przez miejscowość przepływa Żarnica , niewielka rzeka dorzecza Bugu , lewy dopływ Zielawy .
Historia .
Dawniej miasto lokowane w 1504 przez Lwa Bohowitynowicza ( Bohowityna ? )
za zgodą króla Aleksandra Jagiellończyka na prawie magdeburskim pod ówczesną nazwą Lewkowo .
Znany jest przywilej lokacyjny króla Aleksandra z roku 1504 nadający prawo wolnego jarmarku i prawo budowy zamku na miejscu starego grodziska w widłach Zielawy i Żarnicy .
W dokumentach z 1529 roku jest już wymienione miasteczko Dokudów .
W XVI w. było ono własnością Bohowitynów i wchodziło w skład dóbr Ortel ( Wortel ) , w 1580 wzmiankowano w nim fundację monasteru .
O „ horodyszczach “ w rejonie Dokudowa wspominały Akta Grodzkie Brzeskie .
W XVI w. w miejscowości funkcjonował prawosławny klasztor .
