Pięćmorgi
Pięćmorgi ( niem .
`` Fünfmorgen '' ) – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie świeckim , w gminie Jeżewo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) liczyła 111 mieszkańców .
Jest szesnastą co do wielkości miejscowością gminy Jeżewo .
Historia .
Pięćmorgi to kociewska wieś .
Pierwsza historyczna nazwa pochodzi z lat czterdziestych XIX wieku i została zapisana jako Fünfmorgen ( język niemiecki ) .
Następne to : Fünfmorgen , Pięćmorg ( 1886 r. ) , Pięćmorgów ( 1920 r. ) , Pięćmorgi ( 1928 r. ) .
Nazwa wsi jest złożeniem kulturowym z liczebnikiem `` pięć '' w pierwszym członie i rzeczownikiem `` morg '' - mórg ( miara powierzchni ) .
Nazwa jest traktowana jako kalka nazwy niemieckiej .
