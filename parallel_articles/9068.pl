Piątek
Piątek ( skrót pt .
lub piąt . )
– dzień tygodnia między czwartkiem a sobotą .
Według normy ISO-8601 jest piątym dniem tygodnia .
W tradycji biblijnej – chrześcijańskiej i żydowskiej , gdzie za pierwszy dzień tygodnia uznawana jest niedziela , piątek jest dniem szóstym .
Dla chrześcijan jest dniem śmierci Jezusa Chrystusa , w niektórych wyznaniach obowiązuje z tego powodu post .
Piątek jest świętym dniem dla wyznawców islamu .
W judaizmie w piątek , wraz z zachodem słońca , rozpoczyna się szabat .
W kulturze zachodniej w piątek wieczorem rozpoczyna się weekend .
Polska nazwa , wspólna z innymi językami słowiańskimi , pochodzi od liczby `` pięć '' ; por .
