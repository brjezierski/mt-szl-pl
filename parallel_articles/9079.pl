Platerów ( Białoruś )
Platerów ( ) – chutor na Białorusi , w obwodzie witebskim , w rejonie brasławskim , w sielsowiecie Widze .
W Skorowidzach z 1924 i 1933 występuje pod nazwą Platerowo .
Historia .
W czasach zaborów w granicach Imperium Rosyjskiego .
W dwudziestoleciu międzywojennym ówczesna osada fabryczna leżała w Polsce , w województwie nowogródzkim ( od 1926 w województwie wileńskim ) , w powiecie dziśnieńskim ( od 1926 w powiecie brasławskim ) , w gminie Bohiń .
Według Powszechnego Spisu Ludności z 1921 roku osadę zamieszkiwało 217 osób , 147 było wyznania rzymskokatolickiego , 53 prawosławnego , 6 staroobrzędowego a 11 mojżeszowego .
Jednocześnie wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Było tu 40 budynków mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w Wasiewiczach i prawosławnej w Kozianach .
Podlegała pod Sąd Grodzki w Opsie i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Bohiniu .
Na przełomie XIX i XX wieku mieszkała tu z mężem Helena Mniszkówna .
Tu powstał pomysł jej najsłynniejszej powieści : '' Trędowata '' .
