Pleiochaeta setosa
Pleiochaeta setosa ( Kirchn . )
S. Hughes – gatunek workowców .
Patogen wywołujący u łubinu chorobę o nazwie brunatna plamistość liści łubinu .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Incertae sedis , Incertae sedis , Incertae sedis , Incertae sedis , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1892 r. Emil Otto Oskar von Kirchner nadając mu nazwę `` Ceratophorum setosum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Stanley John Hughes w 1951 r .
Anamorfa znana jest jako `` Ceratophorum setosum '' .
Dawniej zaliczana była do grzybów niedoskonałych .
Dla teleomorfy nie określono bliżej jej taksonomii , wiadomo tylko , że należy do workowców .
Morfologia .
