Pleiskirchen
Pleiskirchen – gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Südostoberbayern , w powiecie Altötting .
Leży około 12 km na północny zachód od Altötting .
Najbardziej na północ wysunięta gmina w powiecie .
Polityka .
Rada gminy składa się z 14 osób .
