Boczniak rowkowanotrzonowy
Boczniak rowkowanotrzonowy ( `` Pleurotus cornucopiae '' ( Paulet ) Rolland ) – gatunek grzybów z rodziny boczniakowatych ( `` Pleurotaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pleurotus , Pleurotaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1793 r. J.J. Paulet nadając mu nazwę `` Dendrosarcus cornucopiae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1910 r. Léon Louis Rolland .
Synonimy :
Nazwę polską podał Władysław Wojewoda w 1999 r. Używana jest też nazwa boczniak lejkowaty .
Morfologia .
Średnica 4–12 cm , kształt początkowo łukowaty , później płaski , w końcu wklęsły , a nawet lejkowaty .
Powierzchnia gładka , naga , o barwie białawej , płowożółtej , płowoochrowej , bladobrązowej .
Starsze owocniki blakną .
