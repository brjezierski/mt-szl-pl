Abidżan
Abidżan ( fr .
`` Abidjan '' ) – największe miasto , główny ośrodek gospodarczy , naukowo-kulturalny i turystyczny Wybrzeża Kości Słoniowej , położony na południu kraju , do 1983 roku stolica państwa .
Położenie .
Abidżan położone jest nad laguną Ebrié , która jest połączona z Zatoką Gwinejską kanałem Vridi .
Demografia .
W 2014 roku Abidżan liczył 4,7 mln mieszkańców .
Należy do największych miast Afryki ; liczbą ludności ustępuje jedynie Lagosowi , Kairowi , Kinszasie i Aleksandrii , zbliżoną ma Casablanca ( nie licząc zespołów miejskich Chartumu i Johannesburga ) .
