Pleśnica ( struga )
Pleśnica ( do 1945 r. niem .
`` Bauergraben '' lub `` Bauerbach '' ) – niewielka struga w północno-zachodniej Polsce , w województwie zachodniopomorskim , w powiecie polickim .
Nazwa polska nowa od 1945 r. , topograficzna od rzeczownika `` pleśń '' , nazwa niemiecka kulturowo-topograficzna od rzeczowników `` Bauer '' = '' chłop '' , `` rolnik '' ; `` Graben '' = '' rów '' ; `` Bach '' = '' strumień '' .
Struga wypływa z łąk położonych w południowej części Trzebieży .
Płynie na południe przez Struskie Bagna , uchodzi do rzeczki Karpiny i wraz z nią do Roztoki Odrzańskiej .
W Struskich Bagnach , od strony zachodniej przejmuje wody strumienia Karwia Struga .
Obecnie zmeliorowany nurt Pleśnicy pełni rolę rowu odwadniającego Struskie Bagna .
