Plos ( ros .
Плёс ) – miasto w Rosji w obwodzie iwanowskim .
Jest jednym z głównych centrów turystycznych regionu , położone na prawobrzeżnym brzegu Wołgi , u ujścia rzeki Szochonki .
W odległości 18 km od Priwołżska , i 71 km od Kostromy .
Najwyższy punkt miasta wznosi się na 54 m nad poziomem Wołgi .
Ludność Plosa liczy 1796 osób ( 2017 ) .
W Plosie nie ma zakładów przemysłowych .
