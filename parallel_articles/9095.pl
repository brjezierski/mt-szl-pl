Ploeszti
Ploeszti ( rum .
`` Ploiești '' , do 1960 `` Ploești '' ) – miasto w południowej Rumunii , ośrodek administracyjny okręgu Prahova , na północnym krańcu Niziny Wołoskiej .
W 2002 roku liczyło 232,5 tys .
mieszkańców .
Ploeszti jest ważnym ośrodkiem przemysłu , głównie wydobywczego : w pobliżu miasta znajduje się największa w Rumunii rafineria ropy naftowej ( w miejscowości Brazi ) .
Historia .
Miasto zostało założone w 1596 r. za panowania hospodara wołoskiego Michała Walecznego .
W połowie XIX w. okolice Ploeszti stały się jednym z najważniejszych ośrodków wydobycia i obróbki ropy naftowej w świecie .
