Plougastel-Daoulas ( bret .
`` Plougastell-Daoulaz '' ) – miejscowość i gmina we Francji , w regionie Bretania , w departamencie Finistère .
W 2013 roku populacja gminy wynosiła 13 507 mieszkańców .
Pomiędzy miejscowościami Le Relecq-Kerhuon a Plougastel-Daoulas rzeka Élorn uchodzi estuarium do zatoki Rade de Brest .
