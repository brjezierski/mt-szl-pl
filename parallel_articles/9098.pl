Ploërmel
Ploërmel ( bret .
`` Ploermael '' ) – miejscowość i gmina we Francji , w regionie Bretania , w departamencie Morbihan .
W 2016 roku populacja ówczesnej gminy wynosiła 10 117 mieszkańców .
Dnia 1 stycznia 2019 roku połączono dwie wcześniejsze gminy : Monterrein oraz Ploërmel .
Siedzibą gminy została miejscowość Ploërmel , a nowa gmina przyjęła jej nazwę .
W Ploërmel znajduje się pomnik Jana Pawła II , którego autorem jest rosyjski rzeźbiarz , gruzińskiego pochodzenia Zurab Cereteli .
Pomnik ten stał się znany na świecie z powodu związanych z nim kontrowersji , ponieważ we francuskim prawie od 1905 roku obowiązuje rozdział Kościoła i państwa .
Zakazuje ono umieszczania symboli religijnych w miejscach publicznych .
Niezgodny z prawem był nie sam pomnik , lecz krzyż , symbol religii chrześcijańskiej , który swoim układem i wymiarami ma charakter ostentacyjny .
Dopiero w czerwcu 2018 roku kontrowersje zakończyły się zakupem przez diecezję posągu i jego przemieszczeniem o 30 metrów na grunty nabyte przez diecezję , w sąsiedztwie prywatnej katolickiej uczelni Sacré-Cœur .
