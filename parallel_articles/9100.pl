Drobnołuszczak kosmaty
Drobnołuszczak kosmaty ( `` Pluteus hispidulus '' J.E .
Lange ) – gatunek grzybów należący do rodziny łuskowcowatych ( `` Pluteaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pluteus , Pluteaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1818 r. Elias Fries nadając mu nazwę `` Agaricus hispidulus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Claude-Casimir Gillet w 1876 r .
Synonimy :
Stanisław Domański w 1955 r. opisywał ten gatunek pod nazwą łuskowiec kosmaty , Alina Skirgiełło w 1999 r. jako łuskowiec szorstki .
