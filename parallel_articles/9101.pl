Drobnołuszczak sarni
Drobnołuszczak sarni ( `` Pluteus pouzarianus '' Singer ) – gatunek grzybów należący do rodziny łuskowcowatych ( `` Pluteaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pluteus , Pluteaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał Rolf Singer w 1983 r. w Czechosłowacji .
Synonim : `` Pluteus pouzarianus '' var .
`` albus '' Bonnard 1993 .
Alina Skirgiełło w 1999 r. opisała go pod nazwą łuskowiec czeski .
Władysław Wojewoda w 1998 r. zaproponował nazwę łuskowiec ciemnobrązowy , a w 2003 r. drobnołuszczak sarni .
