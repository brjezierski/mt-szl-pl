Drobnołuszczak czarnołuskowy
Drobnołuszczak czarnołuskowy ( `` Pluteus pseudorobertii `` M.M .
Moser & amp ; Stangl ) – gatunek grzybów należący do rodziny łuskowcowatych ( `` Pluteaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pluteus , Pluteaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonim : `` Pluteus pseudorobertii '' M.M .
Moser ( 1953 ) .
Nazwę polską podał Władysław Wojewoda w 2003 r , wcześniej gatunek ten znany był pod nazwą łuskowiec czarnołuskowy .
Morfologia .
Średnica 2–5 cm , początkowo jest łukowaty lub stożkowaty , później rozpostarty i posiada tępy garb .
Powierzchnia początkowo o barwie jasno-skórzastej lub siwosk-órzastej , później-ciemniejsza – brązowosiwa do czarniawej .
Wierzchołek delikatnie łuskowaty i lekkim oliwkowym odcieniu .
