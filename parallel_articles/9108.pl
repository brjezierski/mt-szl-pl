Pląchoty
Pląchoty – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie golubsko-dobrzyńskim , w gminie Golub-Dobrzyń .
Podział administracyjny .
W latach 1975–1998 miejscowość administracyjnie należała do województwa toruńskiego .
Demografia .
Według Narodowego Spisu Powszechnego ( III 2011 r. ) wieś liczyła 145 mieszkańców .
Jest dwudziestą co do wielkości miejscowością gminy Golub-Dobrzyń .
