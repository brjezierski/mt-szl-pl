Pobłędzie
Pobłędzie ( niem .
`` Pablindszen '' , 1936–1938 `` Pablindschen '' , 1938–1945 `` Zollteich '' ) – osada w Polsce położona w województwie warmińsko-mazurskim , w powiecie gołdapskim , w gminie Dubeninki .
W latach 1975–1998 miejscowość administracyjnie należała do województwa suwalskiego .
W okresie międzywojennym Pobłędzie należało do Prus Wschodnich , znajdowała się tutaj stacja kolejowa .
Po drugiej wojnie światowej wieś weszła w skład Polski .
Ze względu na bliskość granicy z ZSRR ( obecnie z obwodem kaliningradzkim Rosji ) , infrastruktura kolejowa została w większości zdemontowana .
