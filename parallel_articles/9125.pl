Podjazy
Podjazy ( dodatkowa nazwa w j. kaszub .
Pòdjazë ) – wieś kaszubska w Polsce , położona w województwie pomorskim , w powiecie kartuskim , w gminie Sulęczyno , na Pojezierzu Kaszubskim nad wschodnimi brzegami jeziora Gowidlińskiego i nad rzeką Słupią .
Siedziba sołectwa Podjazy , w którego skład wchodzą również Widna Góra i Amalka .
Wieś szlachecka położona była w II połowie XVI wieku w powiecie mirachowskim województwa pomorskiego .
W latach 1975–1998 wieś administracyjnie należała do województwa gdańskiego .
Znajdują się na turystycznym Szlaku Kamiennych Kręgów .
W Podjazach urodził się Ottomar Krefft .
We wsi znajduje się Szkoła Podstawowa im .
Danuty Siedzikówny „ Inki ” założona w 1909 roku .
