Historia .
W przeszłości wieś o nazwie `` Elar '' , zasiedlona już w starożytności .
W 1961 otrzymała obecną nazwę na cześć XIX-wiecznego ormiańskiego pisarza Chaczatura Abowiana , w kolejnych latach została rozbudowana , prawa miejskie uzyskała w 1963 .
W mieście stoi pomnik pisarza .
W mieście znajdują się zabytkowy kościół św .
Stefana oraz nowy kościół św .
Jana Chrzciciela , uroczyście otwarty w 2013 roku przez katolikosa Karekina II przy obecności ówczesnego prezydenta kraju Serża Sarkisjana .
Sport .
Od 1955 do 2006 w mieście działał klub piłkarski Kotajk Abowian , dwukrotny finalista Pucharu Armenii .
Znajduje się tu Stadion Kotajk .
