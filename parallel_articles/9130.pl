Województwo podlaskie
Województwo podlaskie – jednostka podziału administracyjnego Polski , jedno z 16 województw .
Położone jest w północno-wschodniej części kraju .
Przez jego środek przepływa Narew .
Siedzibą władz województwa jest Białystok .
Na dzień 1 stycznia 2022 województwo miało około 1,1 mln mieszkańców .
Obejmuje obszar 20 187,02 km² .
Historia .
