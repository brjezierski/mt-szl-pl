Międzyrzec Podlaski ( stacja kolejowa )
Międzyrzec Podlaski – stacja kolejowa w Międzyrzecu Podlaskim , w województwie lubelskim , w Polsce .
Dworzec kolejowy został otwarty w 1867 roku i znajduje się przy Placu Dworcowym 3 .
W roku 2017 stacja obsługiwała 300–499 pasażerów na dobę .
Stacja znajduje się na międzynarodowej linii E20 Berlin – Warszawa – Moskwa .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Historia .
W czasie II wojny światowej stacja kolejowa w Międzyrzecu Podlaskim a także magistrala kolejowa była celem wielokrotnych ataków partyzantów , którzy podkładając bomby , rozkręcając tory i ostrzeliwując składy paraliżowali niekiedy na wiele tygodni transport niemieckich żołnierzy , broni , amunicji i żywności na front wschodni .
