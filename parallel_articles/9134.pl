Radzyń Podlaski
Radzyń Podlaski – miasto w województwie lubelskim .
Jest siedzibą powiatu radzyńskiego i gminy wiejskiej Radzyń Podlaski .
Według danych GUS z 31 grudnia 2021 r. miasto miało 15 182 mieszkańców .
Historycznie położony jest w Małopolsce , w ziemi łukowskiej , od XIX wieku tereny te określa się również mianem Podlasia Południowego .
W latach 1975–1998 miasto administracyjnie należało do województwa bialskopodlaskiego .
