Podolsk ( Ukraina )
Podolsk ( ukr .
Подільськ , `` Podilśk '' ; do 1935 Birzuła , w latach 1935–2016 Kotowsk ) – miasto na Ukrainie , w obwodzie odeskim , w rejonie podolskim .
Ośrodek przemysłowy .
Historia .
Najstarsza wzmianka o miejscowości Birzuła pochodzi z 1779 roku .
W 1792 na mocy postanowień pokoju w Jassach wraz z zachodnim Jedysanem przypadła Rosji .
Od 1924 do 1940 należała do Mołdawskiej Autonomicznej Socjalistycznej Republiki Radzieckiej .
W 1935 miasto zostało przemianowane na Kotowsk na cześć Grigorija Kotowskiego .
W 1939 Kotowsk liczył 16 795 mieszkańców i był trzecim pod względem liczby mieszkańców miastem republiki po Tyraspolu i Bałcie .
Od 1941 do 1944 pod nazwą Bârzula pod zarządem Rumunii w
Transnistrii .
W grudniu 2013 roku w mieście został zniszczony pomnik Włodzimierza Lenina .
W 2016 roku miasto zmieniło nazwę na Podolsk .
