Abraham Van Helsing
Abraham Van Helsing – postać literacka wywodząca się z powieści Brama Stokera `` Drakula '' ( 1897 ) .
Z pochodzenia Holender , z wykształcenia lekarz i prawnik .
Jest poliglotą .
Wbrew obiegowym opiniom , przy przypadku Draculi po raz pierwszy bezpośrednio zetknął się z wampirami , w zgodnej opinii pozostałych postaci - geniusz .
Jest pierwowzorem m.in .
postaci profesora Bulwera z filmu `` Nosferatu – Symfonia Grozy '' .
Pojawiał się też w kolejnych ekranizacjach powieści `` Drakula '' , m.in .
w filmie `` Dracula '' Francisa F. Coppoli , a także w wielu innych filmach opowiadających o Drakuli oraz w `` Van Helsingu '' Stephena Sommersa , gdzie zmieniono jego imię na Gabriel ( zagrał go tu Hugh Jackman ) .
Postać spopularyzowana została również przez komiksy firmy Marvel .
Jego nazwisko pojawia się także w serii mang i anime `` Hellsing '' , które okazuje się tajną organizacją założoną przez Abrahama Van Hellsinga , zajmującą się walką z wampirami .
