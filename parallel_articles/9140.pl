Podosphaera aphanis ( Wallr . )
U. Braun & amp ; S. Takam .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny wielu gatunków roślin z rodziny różowatych `` Rosaceae '' i mirtowatych `` Myrtaceae '' .
Wywołuje u nich chorobę zwaną mączniakiem prawdziwym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1819 roku C.F .
Wallroth , nadając mu nazwę `` Alphitomorpha aphanis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 roku U. Braun i S. Takam .
Synonimy :
Morfologia .
Tworzy na powierzchni porażonych roślin biały nalot złożony ze strzępek grzybni , konidioforów i powstających na nich konidiów .
Strzępki o szerokości 3–5 μm .
