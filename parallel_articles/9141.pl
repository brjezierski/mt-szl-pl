Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1819 roku Diederich Franz Leonhard von Schlechtendal nadając mu nazwę `` Alphitomorpha fuliginea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 roku Uwe Braun i Susumu Takamatsu .
