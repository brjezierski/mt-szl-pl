Morfologia .
Na obydwu stronach porażonych liści tworzy początkowo pajęczynowatą , białą i cienką , potem coraz grubszą i coraz ciemniejszą , w końcu brązową i wełnistą grzybnię .
Appressoria niewyraźne .
Klejstotecja o wymiarach 60–85 µm , przeważnie w gęstych grupach .
Worki pojedyncze , 8-zarodnikowe , ale czasami trafiają się 6-zarodnikowe .
Przyczepki w różnych liczbach , pod równikiem , kuliste , o średnicy 0,5–5 µm , septowane , przeważnie nierozgałęzione , brązowe .
Konidia owalne , powstające w łańcuszkach , z ciałkami fibrozyny .
Występowanie i siedlisko .
Opisano występowanie `` Podosphaera fuliginea '' w Ameryce Północnej , Azji i Europie .
Jest pospolity , u porażonych roślin wywołuje chorobę o nazwie mączniak prawdziwy .
Często występuje wraz z `` Golovinomyces magnicellulatus '' .
