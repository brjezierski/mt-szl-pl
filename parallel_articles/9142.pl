Podosphaera fusca
Podosphaera fusca ( Fr . )
U. Braun & amp ; Shishkoff – gatunek grzybów należący do mączniakowatych ( `` Erysiphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1812 roku Elias Fries , nadając mu nazwę `` Erysiphe fusca '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 roku Uwe Braun i N. Shishkoff .
