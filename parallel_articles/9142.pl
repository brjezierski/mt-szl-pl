Morfologia i rozwój .
`` Podosphaera fusca '' na roślinach uprawnych występuje głównie w postaci bezpłciowej anamorfy , teleomorfa spotykana jest bardzo rzadko .
Na liściach porażonych roślin anamorfa tworzy biały nalot złożony ze strzępek grzybni , konidioforów i powstających na nich w długich łańcuszkach konidiów .
Konidiofory nierozgałęzione i wyprostowane .
Bezbarwne konidia mają elipsoidalno-owalny kształt i rozmiary 24–40 × 15–25 μm .
Owocniki płciowe typu klejstotecjum są kuliste , ciemnobrązowe lub czarne i najczęściej mają średnicę 70–100 μm .
Zawierają po jednym tylko worku z 8 askosporami .
Zimuje na dwuletnich lub wieloletnich roślinach żywicielskich .
Infekcji pierwotnej dokonują konidia i askospory .
Znaczenie .
Pasożyt wielu gatunków roślin należących do rodzin astrowatych , dyniowatych , jasnotowatych , trędownikowatych , psiankowatych i werbenowatych .
Wraz z gatunkiem `` Golovinomyces orontii '' u roślin z rodziny dyniowatych ( `` Cucurbitaceae '' ) wywołuje chorobę o nazwie mączniak prawdziwy dyniowatych .
