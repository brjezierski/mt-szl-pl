Podosphaera leucotricha
Podosphaera fusca ( Ellis & amp ; Everh . )
E.S .
Salmon – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Wywołuje chorobę o nazwie mączniak prawdziwy jabłoni .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Morfologia .
Grzybnia trwała , cienka , rozpostarta , zbudowana ze strzępek o szerokości 3,5–5 μm .
Strzępki są rozgałęzione z przegrodami .
Zarodniki konidialne typu oidium w długich łańcuchach na krótkim konidioforze .
