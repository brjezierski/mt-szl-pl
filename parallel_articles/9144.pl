Podosphaera macularis
Podosphaera macularis ( Wallr . )
U. Braun & amp ; S. Takam .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny rozwijający się na powierzchni roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1819 r. Carl Friedrich Wallroth nadając mu nazwę `` Alphitomorpha macularis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 r. Uwe Braun i S. Takam .
Niektóre synonimy :
Morfologia .
Grzybnia rozwija się na obydwu powierzchniach liści w postaci białych , początkowo watowatych , potem wojłokowatych plam .
Wytwarza w łańcuszkach konidia o kształcie jajowatym , elispoidalnym lub beczkowatym i rozmiarach 20–33 × 13–20 μm .
Kiełkując tworzą słabo wyróżniające się appressoria , czasami stożkowate lub stopowate .
Konidiofory wzniesione , cylindryczne , 1–3 komórkowe o rozmiarach 40–70 × 9–11 μm .
