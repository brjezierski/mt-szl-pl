Podosphaera mors-uvae
Podosphaera mors-uvae ( Schwein . )
U. Braun & amp ; S. Takam – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny wywołujący u agrestu i porzeczki czarnej chorobę amerykański mączniak agrestu .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1832 r. Lewis David von Schweinitz , nadając mu nazwę `` Erysiphe mors-uvae '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 r. Uwe Braun i S. Takam .
Synonimy :
Morfologia i rozwój .
Na owocach , łodygach , ogonkach liściowych i obydwu stronach liści porażonych roślin rozwija się grzybnia .
Początkowo jest szklista i rzadka , potem trwała , tworząca gęste , watowate kolonie , często zlewające się z sobą i pokrywająca całą powierzchnię liści .
Jej nalot początkowo jest biały , później brązowy .
Strzępki długie , dość grubościenne , żółtawe do brązowych , o szerokości około 4–9,5 µm , gładkie do nieco szorstkich , słabo rozgałęzione i septowane .
Konidiofory wyprostowane , długie , proste .
Konidia w długich łańcuchach , szeroko elipsoidalne , jajowate lub beczkowate , 18–30 × 11–20 µm .
Kleistotecja zwykle w grupach , zanurzone w gęstej warstwie grzybni .
Mają średnicę ok. 70–110 µm i zbudowane są z nieregularnie wielokątnych komórek o średnicy ok. 8–25 µm .
Przyczepki w dolnej ich połowie , nieliczne , raczej grube , o zmiennej długości , przeważnie tak długie jak średnica kleistotecjum lub krótsze , ale czasami do 3 razy dłuższe , szkliste , później wybarwione na brązowo , z przegrodami , cienkościenne lub umiarkowanie grube , gładkie lub nieco chropowate , o szerokości 4–10 µm .
Worki 65–100 × 50–70 µm ( 4– ) 8–zarodnikowe , kształt zmienny , elipsoidalno-jajowaty , niemal cylindryczny , czasami niemal kulisty , 16-26,5 × 8-15 µm .
Zarodniki o wymiarach 20-25 × 12-16 µm .
Uwalniają się po pęknięciu owocnika .
Patogen poraża wszystkie gatunki porzeczek ( `` Ribes '' ) , szczególnie jednak podatna jest na niego porzeczka czarna ( `` Ribes niger '' ) .
W Polsce zimuje na porażonych pędach oraz na opadłych liściach .
Infekcji pierwotnej dokonują powstające na nich askospory roznoszone przez wiatr .
