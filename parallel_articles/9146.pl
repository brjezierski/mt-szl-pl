Podosphaera pannosa
Podosphaera pannosa ( Wallr . )
de Bary – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Wśród roślin uprawnych wywołuje choroby : mączniak prawdziwy brzoskwini , mączniak prawdziwy róży i mączniak prawdziwy śliwy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1819 r. C.F Wallroth nadając mu nazwę `` Alphitomorpha pannosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1870 r. H.A de Bary .
Niektóre synonimy :
Morfologia .
Grzybnia trwała , gęsta , rozpostarta , biała lub szara zbudowana z gęsto splątanych strzępek o szerokości 3–7 μm .
Strzępki są słabo rozgałęzione z przegrodami .
