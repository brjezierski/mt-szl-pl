Podosphaera plantaginis
Podosphaera plantaginis ( Castagne ) U. Braun & amp ; S. Takam .
– gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny roślin z rodzaju babka .
Wywołuje u nich chorobę zwaną mączniakiem prawdziwym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1845 roku Jean Louis Martin Castagne , nadając mu nazwę `` Erysiphe plantaginis '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 2000 roku Uwe Braun i S. Takam .
Synonimy :
Lektotyp : na babce lancetowatej France , Aix , Castagne ( PC ) .
Morfologia .
Tworzy na powierzchni porażonych roślin biały nalot złożony ze strzępek grzybni , konidioforów i powstających na nich konidiów .
Ssawki zapuszcza tylko do skórki porażonych roślin .
Konidiofory wzniesione , cylindryczne , złożone z komórki bazalnej o wysokości 25–38 μm , szerokości 15–20 μm i 1–3 krótszych komórek powyżej komórki bazalnej .
