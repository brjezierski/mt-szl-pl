Podosphaera tridactyla
Podosphaera tridactyla ( Wallr . )
de Bary – gatunek grzybów należący do rodziny mączniakowatych ( `` Erysiphaceae '' ) .
Grzyb mikroskopijny , pasożyt bezwzględny roślin z rodzaju `` Prunus '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Podosphaera , Erysiphaceae , Erysiphales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1833 roku Carl Friedrich Wilhelm Wallroth , nadając mu nazwę `` Alphitomorpha tridactyla '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1870 roku Heinrich Anton de Bary .
