Gąsewo Poduchowne
Gąsewo Poduchowne – wieś w Polsce położona w województwie mazowieckim , w powiecie makowskim , w gminie Sypniewo .
Leży przy drodze drodze wojewódzkiej nr 626 .
Wieś duchowna `` Gąsowo '' położona była w drugiej połowie XVI wieku w powiecie różańskim ziemi różańskiej województwa mazowieckiego .
W 1785 roku Gąsewo wchodziło w skład klucza mąkolińskiego biskupstwa płockiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa ostrołęckiego .
W miejscowości znajduje się kościół , który jest siedzibą parafii św .
Małgorzaty .
W strukturze kościoła rzymskokatolickiego parafia należy do metropolii białostockiej , diecezji łomżyńskiej , dekanatu Krasnosielc .
