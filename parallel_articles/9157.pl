Kasza gryczana
Kasza gryczana – kasza wytwarzana z obłuskanych nasion gryki zwyczajnej .
Ze zmielonej kaszy gryczanej całej ( białej lub prażonej ) otrzymuje się mąkę gryczaną .
Rodzaje kasz gryczanych .
W Polsce , zależnie od sposobu obróbki wyróżnia się :
Czas przechowywania kaszy gryczanej od chwili wyprodukowania wynosi dziesięć miesięcy .
Wytwarzanie .
Przetworzenie gryki na kaszę składa się z następujących etapów :
Wartości odżywcze .
Kasza gryczana jest cenionym źródłem składników odżywczych .
Według danych USDA z 2015 roku , 100 gramów ( surowej ) prażonej kaszy gryczanej dostarcza 346 kcal i zawiera 11,7 grama białka , 2,7 grama tłuszczów , 75 gramów wszystkich węglowodanów , a w nich 10,3 grama błonnika .
Nie zawiera glutenu .
Medycyna .
Ze względu na delikatny smak i właściwości zdrowotne , jeszcze w początku XX wieku łamaną i niepaloną kaszę gryczaną często podawano dzieciom i chorym .
Współcześnie kasza krakowska zalecana jest w dietach wrzodowej , łatwostrawnej , łatwostrawnej bogatobiałkowej , beztłuszczowej ( niskotłuszczowej z ograniczeniem błonnika ) , wątrobowej ( łatwostrawnej z ograniczeniem tłuszczu ) – przy czym kasza gryczana cała jest w nich zabroniona .
Ze względu na brak glutenu , kasze gryczane są zalecane w diecie bezglutenowej .
