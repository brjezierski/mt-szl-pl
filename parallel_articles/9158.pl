Poggio Imperiale ( stacja kolejowa )
Poggio Imperiale ( wł .
`` Stazione di Poggio Imperiale '' ) – stacja kolejowa w Apricena , w prowincji Foggia , w regionie Apulia , we Włoszech .
Znajduje się na linii Adriatica ( Ankona – Lecce ) .
Nazwa stacji nawiązuje do pobliskiej gminy Poggio Imperiale .
Według klasyfikacji RFI ma kategorię `` brązową '' .
