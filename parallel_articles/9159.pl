Pogorzelica ( rzeka )
Pogorzelica – potok Pobrzeża Bałtyckiego , na pograniczu wysoczyzn Damnickiej i Polanowskiej oraz Pojezierza Kaszubskiego .
W początkowym odcinku przepływa przez Jezioro Kozie .
W górnym przepływie rzeka przepływa przez Warcimino i stanowiące rezerwat grodzisko wczesnośredniowieczne Runowo .
Dolny ( ujściowy ) przepływ prowadzi Pradoliną Łeby-Redy do ujścia do rzeki Łeby w Chocielewku .
Powierzchnia zlewni wynosi 94,1 km2 .
Nazwę „ Pogorzelica ” wprowadzono w 1948 roku , zastępując niemiecką „ Mühl - Bach ” .
