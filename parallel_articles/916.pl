Abstoma purpureum
Abstoma purpureum ( Lloyd ) G. Cunn .
– gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1922 r. Curtis Gates Lloyd nadając mu nazwę `` Catastoma purpurea '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Gordon Cunningham w 1926 r .
Synonim : `` Catastoma purpurea '' Lloyd .
Morfologia .
Grzyby z gatunku `` Abstoma purpureum '' wytwarzają bulwiaste , podziemne owocniki .
Mają one 4 cm średnicy i składają się z purpurowej gleby otoczonej cienkim , ciemnofioletowym endoperydium oraz grubym ( do 3 mm ) , twardym fioletowym egzoperydium , zawierającym w swojej strukturze ziarna piasku .
Po dojrzeniu owocnika egzoperydium pęka nieregularnie , uwalniając zarodniki .
Zarodniki `` Abstoma purpureum '' mają pokrój kulisty lub lekko eliptyczny o średnicy ok. 10–14 μm , są intensywnie orzechowo ubarwione , a ich powierzchnia pokryta jest siatkowatym wzorem .
Występowanie i siedlisko .
`` Abstoma purpureum '' jest endemitem występującym w Nowej Zelandii .
Rozwija się na wydmach , w piasku , wykształca owocniki pod jego powierzchnią .
