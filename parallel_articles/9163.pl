Pogórze Śląskie
Pogórze Śląskie ( 513.32 ) ( czes .
`` Slezské podhůří '' ) – pogórze w południowej Polsce i fragmentarycznie północno-wschodnich Czechach , fragment Pogórza Zachodniobeskidzkiego , ograniczony dolinami Olzy na zachodzie i Skawy na wschodzie , oddzielony od Beskidu Śląskiego i Małego na południu , przechodzący w Kotlinę Oświęcimską i Ostrawską na północy .
Jest porozcinane dolinami rzek : Olzy , górnej Wisły , Białej , Soły , Wieprzówki i Skawy .
Pogórze Śląskie zbudowane jest z mało odpornych na denudację serii fliszowych z wkładkami wapieni i cieszynitów .
Złożona struktura podłoża geologicznego ścięta jest przez równinę denudacyjną obniżającą się od 400–450 m n.p.m .
u podnóża progu Beskidu Małego i Beskidu Śląskiego do 280–300 m n.p.m .
na granicy Kotliny Oświęcimskiej .
Powierzchnię pogórza pokrywają gliny , na których rozwinęły się zespoły gleb o średniej produkcyjności .
Dominują gleby brunatnoziemne .
Na ograniczonych powierzchniach występują gleby płowe .
Do niewielkich powierzchni ogranicza się zasięg gleb bielicowych , rozwiniętych na bezwęglanowych i bardzo przepuszczalnych zwietrzelinach .
Gleby deluwialne , związane z łagodnie nachylonymi podnóżami stoków , występują w wielu obszarach , których łączna powierzchnia jest niewielka .
Podobną cechą odznaczają się gleby aluwialne w dnach dolin .
Obszar jest gęsto zaludniony , na jego terenie znajdują się miasta : Cieszyn , Trzyniec ( częściowo ) , Skoczów , Bielsko-Biała , Kęty , Andrychów , Wadowice .
Powierzchnia wynosi 545 km² , szerokość pasma wyżyn 5–15 km , wysokość waha się od 300 do 500 m n.p.m .
