Ocean Spokojny
Ocean Spokojny , Pacyfik , Ocean Wielki – największy , najgłębszy i najstarszy , obok Atlantyku , zbiornik wodny na Ziemi .
Z powierzchnią równą 155,6 mln km² zajmuje 30 % całej powierzchni Ziemi .
Oficjalna polska nazwa tego oceanu , zatwierdzona przez Komisję Standaryzacji Nazw Geograficznych , to Ocean Spokojny .
Położenie i obszar .
Największą rozciągłość południkową osiąga między arktycznym Morzem Beringa a Morzem Rossa ( w obrębie Oceanu Południowego ) przy brzegach Antarktydy , gdzie wynosi ona aż 15,5 tys .
km .
Z kolei największą rozciągłość równoleżnikową ma między wybrzeżem Indonezji a Kolumbii i Peru – jest to 19,8 tys .
km , czyli prawie połowa obwodu kuli ziemskiej i pięć razy więcej niż średnica Księżyca .
Granicami oceanu są wybrzeża : Ameryki Północnej i Południowej na wschodzie , Azji i Australii na zachodzie oraz lody Arktyki i Antarktyki odpowiednio na północy i południu .
Pojemność oceanu szacowana jest na 764,836 mln km³ , co stanowi 46 % ziemskich zasobów wód .
Tradycyjnie dzieli się go wzdłuż równika na część północną i południową , ale wyjątkami są tutaj wyspy Gilberta i Galapagos w całości uznawane za Południowy Pacyfik .
Na tym Oceanie znajduje się Rów Mariański , który osiąga głębokość 10 924 m , oraz druga co do wielkości wyspa świata Nowa Gwinea .
Średnia głębokość wynosi 4280 m p.p.m .
Granice .
Granice oceanów są umowne i niejednokrotnie stanowią przedmiot dyskusji naukowców , ponieważ niełatwo je wyznaczyć – na wodzie nie ma naturalnych granic .
Ogólnie przyjmuje się , że Ocean Spokojny oddzielony jest od pozostałych w następujących miejscach :
Morza , wyspy i rzeki .
Morza .
Liczba mórz na Oceanie Spokojnym w stosunku do jego powierzchni nie jest zbyt wielka , ponieważ większa część jego obszaru to po prostu ocean , bez miejsc , w których można wyznaczyć morza .
Jedynie w zachodniej części jest dość gęsta sieć mórz i zatok .
Niektóre z mórz to : Tasmana , Koralowe , Banda , Jawajskie , Celebes , Południowochińskie , Wschodniochińskie , Japońskie , Ochockie oraz Morze Beringa .
Wśród zatok można wyróżnić : Tajlandzką , Karpentaria oraz na wschodzie Alaska i Kalifornijską .
Morza układu oceanicznego – Ocean Spokojny ( Pacyfik ) :
Wyspy .
Na oceanie tym znajduje się około 25 tys .
wysp , czyli więcej niż na pozostałych zbiornikach wodnych razem wziętych .
Duża ich część nosi nazwę Oceanii .
Tradycyjnie składają się na nie :
Prócz tego jest wiele wysp azjatyckich , nie zaliczanych do tego podziału , jak np .
Wyspy Japońskie , Archipelag Filipiński , Borneo i Jawa .
Rzeki .
Do Oceanu Spokojnego wpływa niewiele wielkich rzek .
