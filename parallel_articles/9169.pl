Pokośno
Pokośno – wieś w Polsce położona w województwie podlaskim , w powiecie sokólskim , w gminie Suchowola .
Wieś królewska ekonomii grodzieńskiej położona była w końcu XVIII wieku w powiecie grodzieńskim województwa trockiego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa białostockiego .
W Pokośnie urodził się Tadeusz Komendant – polski krytyk literacki , tłumacz , eseista , pisarz , doktor nauk humanistycznych , wykładowca na Wydziale Polonistyki Uniwersytetu Warszawskiego .
Wierni kościoła rzymskokatolickiego należą do parafii św .
Apostołów Piotra i Pawła w Suchowoli .
