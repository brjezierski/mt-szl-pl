Pokrzywna ( rzeka )
Pokrzywna – rzeka w Polsce , w województwie pomorskim , w powiecie bytowskim , przepływająca przez obszar dwóch gmin – Kołczygłowy i Trzebielino .
Początek bierze we wsi Witanowo w gminie Kołczygłowy .
Dalej płynie południkowo przez obręby ewidencyjne Łobzowo i Barkocin , stanowiąc na znacznej długości granicę między nimi .
Wpływając w granice gminy Trzebielino , obiera północno-zachodni kierunek spływu .
Dalej przepływa przez obręby Poborowo i Trzebielino , oddzielając ten drugi od obrębu Cetyń .
W pobliżu osady Glewnik obiera orientację południową i południowo-zachodnią .
Uchodzi do Wieprzy w obrębie Bożanka na wysokości około 69,5 m n.p.m .
W górnym biegu wpada do Jeziora Granicznego , z którego wypływa na wysokości około 144,2 m n.p.m .
