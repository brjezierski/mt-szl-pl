Pokémon ( anime )
Pokémon , w Japonii – japoński telewizyjny serial anime emitowany od 1 kwietnia 1997 r. na antenie TV Tokyo .
Polska premiera serialu odbyła się 4 września 2000 r. Początkowo serial emitowany był na antenie Polsatu , następnie w TV4 , Fox Kids , Jetix i Disney XD .
Od 2018 roku odcinki dostępne są również na platformie Netflix Polska .
Spis serii .
W Japonii odcinki anime Pokémon podzielone są na 7 serii , jednak poza nią stosowany jest podział na sezony .
Filmy .
Równomierne do sezonów anime wydawane są też .
Według stanu na 2020 rok , wydano 23 filmy .
