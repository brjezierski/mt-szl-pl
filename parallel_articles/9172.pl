Pokój ( książka )
Pokój ( ang .
`` Room '' ) – powieść z 2010 autorstwa irlandzko-kanadyjskiej pisarki Emmy Donoghue .
Opowieść opowiedziana jest z perspektywy pięcioletniego chłopca , Jacka , który jest uwięziony w małym pokoju razem z matką .
Donoghue postanowiła napisać tę historię po usłyszeniu o pięcioletnim Felixie ze Sprawy Josefa Fritzla .
Powieść została znalazła się na liście Orange Prize w 2011 roku i wygrała nagrodę regionalną Commonwealth Writers ' Prize , także w 2011 ( Karaiby i Kanada ) .
W 2010 powieść została nominowana do the Man Booker Prize , Rogers Writers ' Trust Fiction Prize , a także Governor General 's Awards .
Książka pojawiła się w Polsce w 2011 roku nakładem wydawnictwa Sonia Draga .
Tłumaczyła ją Ewa Borówka .
Filmowa adaptacja , nosząca ten sam tytuł , ukazała się w październiku 2015 roku .
Zagrali w niej Brie Larson i Jacob Tremblay .
Film odniósł komercyjny sukces , zbierał pozytywne opinie krytyków i otrzymał cztery nominacje podczas 88. gali rozdania Oscarów , w tym za najlepsze zdjęcia i najlepszą aktorkę dla Larson .
Fabuła .
Jack obchodzi swoje piąte urodziny .
Zna jedynie życie w Pokoju , czyli zamkniętym pomieszczeniu , w którym przebywa wraz ze swoją matka .
Czasem w nocy odwiedza ich Stary Nick , który wzbudza w chłopcu lęk : mężczyzna lata temu porwał jego matkę i przetrzymuje ją razem z synem w szopie , w którym obydwoje mieszkają .
Wkrótce po piątych urodzinach chłopca jego matka postanawia zorganizować ucieczkę .
Tworzy plan , w trakcie którego Jack musi udawać chorobę , a następnie śmierć , aby wydostać się z Pokoju i wezwać pomoc .
