Polanica-Zdrój ( stacja kolejowa )
Polanica-Zdrój – stacja kolejowa w Polanicy-Zdroju , w Polsce , w województwie dolnośląskim , w powiecie kłodzkim .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2017 stacja obsługiwała 150–199 pasażerów na dobę .
Lokalizacja .
Stacja kolejowa położona na linii kolejowej z Kłodzka do Kudowy-Zdroju .
Znajduje się na niezelektryfikowanej trasie o znaczeniu lokalnym , w pewnym oddaleniu od centrum Polanicy-Zdroju , dawniej na terenie wsi Sokołówka ( ) , wchodzącej do 1952 w skład gminy wiejskiej Polanica Zdrój , a w tej części w 1954 włączonej do Polanicy ( całą wieś włączono do tego miasta w 1973 , wraz z Nowym Wielisławiem ) .
Historia .
W latach 80 .
XIX w. postanowiono połączyć Kłodzko , stolicę regionu z uzdrowiskami , położonymi na zachodzie powiatu kłodzkiego .
Do realizacji tego przedsięwzięcia przystąpiono w drugiej połowie tej dekady .
Pierwszy fragment linii do Szczytnej wybudowano w latach 1886–1890 .
W tym czasie zbudowano przystanek osobowy dla obsługi podróżnych i niewielkiego ruchu towarowego .
Do tego celu służył dworzec z poczekalnią i małym magazynem .
15 listopada 1890 wjechał tu pierwszy pociąg .
