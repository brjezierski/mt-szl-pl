Polinezja
Polinezja ( z `` polys '' „ liczny ” + `` nesos '' „ wyspa ” ) – wschodnia część Oceanii , położona w środkowej części Oceanu Spokojnego , po obu stronach równika i po obu stronach linii zmiany daty .
Wyspy Polinezji są pochodzenia wulkanicznego lub koralowego .
Klimat wysp jest gorący i wilgotny ; porasta je bujna roślinność międzyzwrotnikowa .
Etymologia .
Polinezja – pierwszym , który użył tej nazwy był Portugalczyk Joāo de Barros w dziele `` As Decadas de Asia ... '' 1552 do 1563 .
Rozumiał jednak przez to tylko wyspy Australazji , które istotnie były dla niego ogromnym obszarem „ wielowyspia ” .
Dopiero w 1810 geograf duńsko-francuski Conrad Malte-Brun wprowadził nazwę Oceania .
Ogólna powierzchnia wysp tej części Oceanii wynosi 25,8 tys .
