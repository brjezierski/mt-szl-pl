Polacy
Polacy – naród zachodniosłowiański ( lechicki ) zamieszkujący głównie obszar Rzeczypospolitej Polskiej i będący jej głównym składnikiem ludnościowym , a poza granicami Polski tworzący Polonię .
Polacy posługują się w większości językiem polskim oraz alfabetem łacińskim , który został wprowadzony w X wieku z chwilą , od której na ziemiach polskich zaczęto wprowadzać chrześcijaństwo obrządku zachodniego .
W ukształtowaniu wyznaniowym Polaków wyraźną większość stanowią katolicy obrządku łacińskiego , obecne są także inne obrządki i wyznania .
Według Narodowego Spisu Powszechnego z 2011 roku liczba osób deklarujących narodowość polską zamieszkałych na terenie Polski wynosi 36,5 mln ( 37,4 mln ) przy populacji 38,5 mln .
Liczebność diaspory polskiej według różnych źródeł to 13–16 mln .
Stowarzyszenie „ Wspólnota Polska ” szacuje liczebność Polonii na 20 mln .
