Polokwane
Polokwane ( dawniej `` Pietersburg '' , wymowa afrikaans : [ ] ) – miasto w Republice Południowej Afryki , stolica prowincji Limpopo .
Biali stanowią w mieście 40 % mieszkańców .
Położone na wysokości 1312 m n.p.m. , znajdowało się na głównym odcinku szlaku wiodącego na północ , do Zimbabwe .
Założone zostało przez przybyłych tu w 1886 roku Burów oraz nazwane Pietersburg na cześć generała Petrusa Jacobusa `` Piet '' Joubera .
Podczas wojen burskich miasto stało się stolicą republiki Transwalu .
W 1901 roku miasto zostało zdobyte przez Brytyjczyków .
W roku 2002 rząd południowoafrykański rozpoczął ogólnokrajową zmianę nazw miast , których nazwy przypominały politykę apartheidu .
Nazwany przez białych `` Pietersburg '' został więc przechrzczony w 2003 roku na obecną nazwę Polokwane .
Nazwa ta pochodzi z języka pedi i oznacza w dosłownym tłumaczeniu `` pewne miejsce '' .
W roku 1984 Polokwane zamieszkiwało 79 000 ludzi ( z których 14 000 stanowili biali ) .
Obecnie miasto liczy 440 111 mieszkańców ( 2003 ) .
Polokwane jest ośrodkiem wydobywczym diamentów .
Dzięki położeniu na wysokości 1312 metrów miasto posiada przyjemny klimat z przeciętnymi temperaturami 27 °C w lecie i 20 °C w zimie .
Liczba opadów w lecie oscyluje pomiędzy 400 a 600 mm .
Polokwane było jednym z gospodarzy Mistrzostw Świata w Piłce Nożnej 2010 w RPA .
