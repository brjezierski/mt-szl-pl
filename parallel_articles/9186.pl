Polonia Bytom ( koszykówka )
Polonia Bytom ( koszykówka ) – sekcja sportowa klubu Polonia Bytom istniejąca do 1981 roku , a następnie reaktywowana w 2013 roku .
W sezonie 2022/2023 drużyna seniorów występuje w rozgrywkach I ligi ( drugi poziom rozgrywkowy ) .
Trenerem koszykarzy jest Mariusz Bacik .
Sekcja koszykówki męskiej .
Na przełomie lat 60/70 .
XX w. koszykarze Polonii Bytom awansowali z A-klasy do ówczesnej III ligi , a następnie do drugiej ligi w sezonie 1977/78 .
Jednym ze współautorów tego sukcesu był kierownik sekcji Jan Stankowski .
Skład Polonii z 1979 : Leszek Marzec , Janusz Kropka , Jacek Rosa , Krzysztof Oborski , Andrzej Drzyzga , Krystian Rendchen , Jan Kańtoch , Ryszard Stankiewicz , Henryk Kominek .
Trener : Jerzy Hermanson .
Skład Polonii w 1980 : Jacek Rosa , Sławomir Jezior , Ryszard Stankiewicz , Henryk Kominek , Bogusław Bańka , Kazimierz Boboń , Zbigniew Pierechod , Adam Prabucki , Marek Kuna , Krystian Rendchen , Jerzy Kawalec , Ryszard Szopa , Mariusz Dziurdzia .
Trener Jerzy Kapuściński .
W sezonie 1980/81 KS Polonia awansowała do pierwszej ligi .
W związku z trudną sytuacją finansową ( 3 drużyny w pierwszej lidze , dodatkowo piłkarze i hokeiści ) dnia 4 maja 1981 sekcję koszykówki Polonii Bytom przejął reaktywowany w Bobrku ( dzielnicy Bytomia ) , we wrześniu 1980 , Hutniczy Klub Sportowy Stal Bobrek ( Bobry Bytom ) .
W kolejnych latach próbowano kilkukrotnie reaktywować sekcję koszykówki mężczyzn .
Z uwagi na brak pieniędzy , nie udało się tego zrealizować .
Ostatecznie latem 2013 roku wznowiono funkcjonowanie sekcja koszykówki mężczyzn .
Została ona zgłoszona do udziału w rozgrywkach III ligi , w sezonie 2013/2014 .
Po rocznej grze w III lidze Polonia awansowała do II ligi .
Swoje mecze rozgrywa w hali Na Skarpie .
W sezonie 2021/22 drużyna trenera Mariusza Bacika zajęła pierwsze miejsce w grupie C ( runda zasadnicza ) , a następnie w fazie play-off wyeliminowała Niedźwiadki Chemart Przemyśl , Sklep Polski MKK Gniezno oraz ŁKS Coolpack Łódź .
W półfinale Polonia Bytom przegrała z drużyną Enea Basket Poznań , co skutkowało konfrontacją o trzecie miejsce z AZS AWF Mickiewicz Romus Katowice .
Niebiesko-czerwoni przy wypełnionej hali wygrali u siebie pierwsze spotkanie 90:77 , a w rewanżu ponowie zwyciężyli 84:82 , dzięki czemu awansowali do I ligi i po 21. latach powrócili na wyższy szczebel rozgrywkowy .
Sekcja koszykówki kobiet .
W Polonii Bytom sekcja działała do 1986 roku , potem została przejęta przez HKS Stal Bobrek .
