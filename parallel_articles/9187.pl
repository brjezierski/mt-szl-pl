Polska Federacja Pétanque
Polska Federacja Pétanque – polski związek sportowy , zrzeszający kluby sportowe specjalizujące się w Pétanque , zrzeszony w Międzynarodowej Federacji Pétanque i Gry Prowansalskiej ( FIPJP ) Siedziba Federacji znajduje się we Wrocławiu .
PFP powstała 10 listopada 2002 , a od roku 2003 jest członkiem FIPJP
Zarząd .
Skład Zarządu PFP .
Zarząd PFP wybierany jest przez zgromadzenie delegatów Kongresu Sprawozdawczo-Wyborczego na okres dwu lat .
Jako pierwszego dokonuje się wyboru Prezesa .
Następnie Zgromadzenie wybiera pozostałych członków Zarządu .
Zarząd ma 14 dni na ukonstytuowanie się .
