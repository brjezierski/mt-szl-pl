Alfabet polski
Alfabet polski , abecadło ( dawniej : `` obiecadło '' , `` obiecado '' ) – alfabet dwuszeregowy oparty na alfabecie łacińskim używany do zapisu języka polskiego .
Litery .
Oparty jest na piśmie łacińskim i składa się obecnie z 32 liter , z czego 9 ma znaki diakrytyczne .
Każda litera może występować w formie majuskuły lub minuskuły , przy czym majuskuła litery `` y '' ( `` Y '' ) występuje jedynie w wyjątkach , m.in .
w zapożyczeniach i pisowni historycznej .
Dodatkowo używane są litery `` q '' , `` v '' i `` x '' w niektórych wyrazach pochodzenia obcego , czyli zapożyczonych bądź w pisowni historycznej .
Litery te są też używane w szeregowaniu alfabetycznym wyrazów zgodnie z ich miejscem w alfabecie łacińskim .
Z tych powodów litery te są niekiedy zaliczane do alfabetu polskiego , w takim przypadku alfabet polski liczy 35 liter .
