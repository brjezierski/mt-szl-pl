Abu Dis
Abu Dis ( arab .
أبو ديس ) – miasto w Autonomii Palestyńskiej ( Zachodni Brzeg , muhafaza Jerozolima ) .
Graniczy z Jerozolimą .
Od 1995 po porozumieniu pokojowym z Oslo , jest częścią terenu „ B ” pod wspólną izraelsko-palestyńską kontrolą .
Według Palestyńskiego Centralnego Biura Statystycznego w 2007 liczyło 10 782 mieszkańców .
