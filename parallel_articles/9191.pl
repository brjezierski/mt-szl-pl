Polska Cerekiew ( stacja kolejowa )
Polska Cerekiew – stacja towarowa położona we wsi Polska Cerekiew .
W przeszłości również obsługiwała ruch pasażerski , najpierw jako stacja , a następnie jako przystanek .
Historia .
Stacja Polska Cerekiew przed 1945 nosiła nazwę `` Groß Neukirch '' .
Od 1898 do 2004 istniała jako stacja pasażerska .
Po 2004 linia została zamknięta .
