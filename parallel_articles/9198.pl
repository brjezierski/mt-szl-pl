I liga polska w piłce nożnej ( 2018/2019 )
I liga polska 2018/2019 – 71. edycja ogólnokrajowych rozgrywek drugiego poziomu ligowego piłki nożnej mężczyzn w Polsce .
Ich sponsorem tytularnym były zakłady bukmacherskie Fortuna , w związku z czym obowiązywała nazwa marketingowa Fortuna I liga .
Zmagania rozpoczęły się 20 lipca 2018 , a zakończyły się 18 maja 2019 .
Ich organizatorem był Polski Związek Piłki Nożnej .
Rywalizacja toczyła się systemem kołowym , a wzięło w niej udział 18 drużyn .
Łącznie rozegrano 306 spotkań , w których strzelono 765 bramek ( średnia 2,5 gola na mecz ) .
Tytuł mistrzowski wywalczył .
Pierwszą bramkę strzelił w 13. minucie meczu – zawodnik gości Martin Dobrotka ( spotkanie zakończyło się wynikiem 0:2 ) .
