Żagiewka wielkopora
Żagiewka wielkopora ( `` Neofavolus alveolaris '' ( DC . )
Sotome & amp ; T .
Hatt . )
– gatunek grzybów z rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Neofavolus , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1815 r. Augustin Pyramus de Candolle nadając mu nazwę `` Merulius alveolaris '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu K. Sotome i T. Hattori w 2013 r. r .
Ma 18 synonimów .
Niektóre z nich :
Stanisław Domański w 1967 r. nadał polską nazwę żagiew morwowa , Władysław Wojewoda w 2003 r. zmienił ją na żagiew wielkopora .
