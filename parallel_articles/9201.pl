Twardziak włosistobrzegi
Twardziak włosistobrzegi , żagiew włosistobrzega ( `` Lentinus arcularius `` ( Batsch ) Zmitr . )
– gatunek grzybów należący do rodziny żagwiowatych ( `` Polyporaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Lentinus , Polyporaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1783 r. August Johann Batsch nadając mu nazwę `` Boletus arcularius '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 2010 r. Iwan Zmitrowicz , przenosząc go do rodzaju `` Lentinus '' .
Synonimów ma około 50 .
