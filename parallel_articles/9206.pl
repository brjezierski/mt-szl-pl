Demografia .
Według danych pięcioletnich z 2020 roku , 58,5 % mieszkańców stanowiła ludność biała ( 41,8 % , nie licząc Latynosów ) , 30,2 % to czarnoskórzy Amerykanie lub Afroamerykanie , 7,0 % miało rasę mieszaną , 1,7 % to Azjaci , 0,15 % to rdzenna ludność Ameryki , 0,006 % to Hawajczycy i mieszkańcy innych wysp Pacyfiku .
Latynosi stanowią 23,9 % ludności miasta .
Poza osobami pochodzenia afroamerykańskiego , do największych grup należą osoby pochodzenia haitańskiego ( 8,4 % ) , włoskiego ( 7,1 % ) , niemieckiego ( 7 % ) , irlandzkiego ( 6,9 % ) , meksykańskiego ( 5,4 % ) , „ amerykańskiego ” ( 5,2 % ) , angielskiego ( 5,2 % ) , portorykańskiego ( 3,5 % ) i kubańskiego ( 3,1 % ) .
