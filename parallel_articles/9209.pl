Ponferrada
Ponferrada – miasto w Hiszpanii , w prowincji León , nad rzeką Sil .
W 2019 r. miasto to na powierzchni 283 km² zamieszkiwało 64 674 osób .
W tym mieście rozwinął się przemysł chemiczny , przemysł metalowy , przemysł maszynowy , przemysł elektrotechniczny oraz przemysł spożywczy .
W 2014 roku odbyły się tutaj Mistrzostwa Świata w Kolarstwie Szosowym .
W Ponferradzie , położonej przy szlaku pielgrzymkowym do grobu Św .
Jakuba w Santiago de Compostela , znajduje się średniowieczna twierdza zakonu Templariuszy , którzy zostali sprowadzeni do miasta przez króla Leonu Ferdynanda II w 1178 , by chronić pielgrzymów .
