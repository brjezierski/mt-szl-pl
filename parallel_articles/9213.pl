Pont-l ’ Évêque ( ser )
Pont-l ’ Évêque – francuski ser pleśniowy wytwarzany z mleka krowiego .
Ser ma formę prostopadłościennych bloczków , zwykle o kwadratowej podstawie o boku długości od 9 do 21 cm ( najczęściej 11 cm ) i wysokości 3,5 cm .
Produkowana jest też odmiana `` demi Pont-l ’ Évêque '' o podstawie prostokąta 11 × 5,5 cm .
Pokrywa go biała skórka , z żółtymi lub czerwonawymi przebarwieniami i podłużnymi wyżłobieniami .
Wnętrze sera jest kremowożółte i ma półmiękką konsystencję , występują w nim niewielkie dziurki .
Receptura sera wywodzi się z XIII wieku , opracowana przez cysterskich mnichów w okolicach miasta Pont-l ’ Évêque , w Normandii .
Ser początkowo znany był pod nazwą `` angelot '' ( z fr .
„ cherub ” ) , od monety o tej samej nazwie , wykorzystywano go bowiem jako środek płatniczy .
Produkcja sera chroniona i regulowana jest we Francji w ramach systemu `` Appellation d ’ origine contrôlée '' ( AOC ) .
Na szczeblu europejskim ser objęty jest chronioną nazwą pochodzenia .
Produkcja sera Pont-l ’ Évêque odbywać się może wyłącznie na terenie normandzkich departamentów Calvados , Eure , Manche i Orne .
