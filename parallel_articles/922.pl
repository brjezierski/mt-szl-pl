Abudża
Abudża ( ang .
`` Abuja '' ) – miasto w Nigerii , od 1991 stolica tego państwa , leży w stanie Federalne Terytorium Stołeczne .
Węzeł komunikacyjny , port lotniczy , uniwersytet .
W 1976 z powodu przeludnienia Lagos zdecydowano się przenieść stolicę państwa do innego miasta .
Wybrano Abudżę , gdyż była położona w centrum kraju .
Miasto Abudża zostało wybudowane od podstaw w latach 80 .
XX wieku na obszarze wzgórz Chukuku .
Nowe miasto zaplanował japoński architekt Kenzō Tange .
Oficjalnie została ogłoszona stolicą Nigerii w 1991 .
Jej populacja , zgodnie z danymi z 1991 , wynosiła 378,7 tys. , obecnie liczba mieszkańców wynosi ponad 1 mln .
