Popioły ( rejon szarkowszczyński )
Popioły – dawny zaścianek .
Tereny na których leżał znajdują się obecnie na Białorusi , w obwodzie witebskim , w rejonie szarkowszczyńskim , w sielsowiecie Hermanowicze .
Historia .
W czasach zaborów zaścianek w powiecie dzisieńskim , w guberni wileńskiej Imperium Rosyjskiego .
W latach 1921–1945 zaścianek leżał w Polsce , w województwie wileńskim , w powiecie dziśnieńskim , w gminie Hermanowicze .
Według Powszechnego Spisu Ludności z 1921 roku zamieszkiwało tu 11 osób , wszystkie były wyznania rzymskokatolickiego .
Jednocześnie 8 mieszkańców zadeklarowało polską a 3 białoruską przynależność narodową .
Były tu 3 budynki mieszkalne .
W 1931 w 3 domach zamieszkiwało 15 osób .
Wierni należeli do parafii rzymskokatolickiej w Hermanowiczach .
Miejscowość podlegała pod Sąd Grodzki w Łużkach i Okręgowy w Wilnie ; właściwy urząd pocztowy mieścił się w Hermanowiczach .
