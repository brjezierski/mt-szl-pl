Popcorn ( czasopismo )
Popcorn – miesięcznik o charakterze okołorozrywkowym dla nastolatków wydawany w Polsce przez niemiecki koncern Axel Springer .
Pierwszym redaktorem naczelnym był Marek Adamski , a redakcja mieściła się we Wrocławiu .
Początkowo wydawała go spółka Marquard Media Polska , a od 2000 roku Ringier Axel Springer Polska .
W pierwszej dekadzie XXI wieku sprzedaż znacząco spadła ( w 2009 roku średnio prawie 106 tysięcy , w 2011 średnio prawie 58 tysięcy ) i wydawca w marcu 2011 roku połączył redakcję z magazynem „ Dziewczyna ” , a następnie zdecydował o zamknięciu tytułu .
W styczniu 2012 roku ukazał się ostatni numer magazynu .
Ostatnim redaktorem naczelnym była Ewa Mościcka .
