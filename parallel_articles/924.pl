Abéché
Abéché ( Abeszr ) – miasto we wschodnim Czadzie , położone na wyżynie Wadaj .
Ludność : 78,2 tys .
( 2008 ) .
Abéché jest ośrodkiem administracyjnym Regionu Wadaj , departament Ouara .
Czwarte co do wielkości miasto kraju .
Miasto leży przy drodze samochodowej z Ndżameny do Sudanu , na skrzyżowaniu dawnych szlaków karawanowych : wschód–zachód i transsaharyjskiego ( do Bengazi , nad Morze Śródziemne ) .
Ośrodek handlowy regionu hodowli bydła .
Występuje przemysł spożywczy .
Znajduje się tu port lotniczy oraz liczne meczety .
Od połowy XIX wieku Abéché było stolicą sułtanatu Wadaj .
W XVI–XIX w. także ośrodek handlu niewolnikami .
