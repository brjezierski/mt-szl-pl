Por la razón o la fuerza
Por la razón o la fuerza ( hiszp .
`` Rozumem lub siłą '' ) – oficjalna dewiza państwowa Chile .
Stanowi swobodne tłumaczenie łacińskiej sentencji `` Aut consiliis aut ense '' ( `` Radą lub szpadą '' ) , która pojawiła się w 1812 roku na pierwszym herbie Chile .
Obecna wersja pochodzi z 1818 roku i po raz pierwszy umieszczono ją na monetach .
Od 1834 roku zdobi kolejne wersje herbu Chile , chociaż do oficjalnego herbu państwowego została włączona dopiero w roku 1920 .
Pierwotnie motto miało wyrażać prymat środków pokojowych nad militarnymi ( słowo `` rozum '' po prawej heraldycznej , `` siła '' po lewej ) , ale też gotowość obrony kraju z bronią w ręku .
W roku 2004 powstał projekt zmiany motta na `` Por la fuerza de la razón '' ( hiszp .
`` siłą rozumu '' ) , wniesiony przez senatora Nelsona Ávilę a tłumaczony zbyt militarnym charakterem dotychczasowej dewizy , nie zyskał on jednak akceptacji parlamentu .
