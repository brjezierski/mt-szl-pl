Porajów ( przystanek kolejowy )
Porajów – zlikwidowany przystanek osobowy w Porajowie , położony w województwie dolnośląskim , w powiecie zgorzeleckim , w gminie Bogatynia .
Przystanek leży na tranzytowej linii kolejowej z Czech do Niemiec , ostatnie regularne połączenia odbywały się przed wojną kiedy to istniała tu infrastruktura kolejowa .
5 grudnia 2009 z okazji 150. rocznicy uruchomienia linii kolejowej Liberec – Hradek nad Nysą – Zittau w Porajowie zatrzymał się po raz pierwszy od 1945 roku pociąg pasażerski .
Pociągiem podróżowali m.in .
przedstawiciele samorządów Hradka , Zittau i Bogatyni .
Przejazd zorganizowano w intencji włączenia Porajowa ( jako przystanku kolejowego ) w ruchu międzynarodowym .
