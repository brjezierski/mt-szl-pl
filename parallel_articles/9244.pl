Skórnikowiec szarobrązowy `` Porostereum spadiceum '' ( Pers . )
Hjortstam & amp ; Ryvarden – gatunek grzybów należący do rodziny korownicowatych ( `` Phanerochaetaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phaneropchaete , Phanerochaetaceae , Polyporales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Thelephora spadicea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu w 1990 r. Kurt Hjortstam i Leif Ryvarden , przenosząc go do rodzaju `` Porostereum '' .
Synonimów ma około 30 .
