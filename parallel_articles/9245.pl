Kamusznik siny
Kamusznik siny , krążniczka sina ( `` Porpidia tuberculosa '' ( Sm . )
Hertel & amp ; Knoph ) – gatunek grzybów z rodziny krążniczkowatych ( `` Lecideaceae '' ) .
Ze względu na współżycie z glonami zaliczany jest do porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Porpidia , Lecideaceae , Lecideales , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1885 r. James Edward Smith nadając mu nazwę `` Spiloma tuberculosums '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Hannes Hertel i Johannes-Günther Knoph w 1984 r .
