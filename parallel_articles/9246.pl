Gąsownica czarnoziarnista
Gąsownica czarnoziarnista ( `` Porpoloma elytroides '' ( Scop . )
Singer ) – gatunek grzybów należący do rzędu pieczarkowców ( `` Agaricales '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Porpoloma , Tricholomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1772 roku Giovanni Antonio Scopoli nadając mu nazwę `` Agaricus elytroides '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Rolf Singer w1973 r .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Synonimy :
Występowanie i siedlisko .
Znane jest występowanie gąsownicy czarnoziarnistej tylko w niektórych krajach Europy .
Władysław Wojewoda w zestawieniu wielkoowocnikowych grzybów podstawkowych Polski przytacza tylko jedno stanowisko ( Tatrzański Park Narodowy 1956 ) z uwagą , że rozprzestrzenienie i stopień zagrożenia tego gatunku w Polsce nie są znane .
W Europie występuje rzadko , na rozproszonych stanowiskach .
Znajduje się na czerwonych listach gatunków zagrożonych w Szwajcarii , Niemczech , Danii , Wielkiej Brytanii , Norwegii , Szwecji .
Występuje na dawnych , żyznych , nieużytkowanych łąkach , głównie na terenach górskich , aż do strefy subalpejskiej , ale także na nizinach .
