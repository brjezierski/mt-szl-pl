Gqeberha
Gqeberha ; ( 1820–2021 Port Elizabeth ) – miasto w południowej części Republiki Południowej Afryki , w Prowincji Przylądkowej Wschodniej , w aglomeracji Nelson Mandela Bay , nad zatoką Algoa Oceanu Indyjskiego .
W 2020 roku liczyło ok. 967 tys .
mieszkańców .
Ważny ośrodek handlu oraz przemysłu samochodowego , stoczniowego , maszynowego , spożywczego , chemicznego , cementowego , włókienniczego , drzewnego , papierniczego , skórzano-obuwniczego , szklarskiego , petrochemicznego ( rafineria ropy naftowej ) .
Ponadto duży port handlowy i rybacki , a także ważny węzeł komunikacyjny .
Od 1964 r. w mieście działa uniwersytet .
Miejscowość została założona w 1820 roku przez kolonistów brytyjskich na miejscu Fortu Frederick wzniesionego w 1799 roku .
Status miasta otrzymało Port Elizabeth w 1913 roku .
Nazwę nadał Sir Rufane Donkin , gubernator Kolonii Przylądkowej , na cześć zmarłej żony Elizabeth .
W mieście znajduje się Nelson Mandela Bay Stadium , na którym odbywały się finały Mistrzostw Świata w Piłce Nożnej 2010 i Puchar Narodów Afryki 2013 .
Przez miejscowość przebiega droga krajowa nr 2 z Kapsztadu do Ermelo .
23 lutego 2021 przyjęta została nowa nazwa miasta `` Gqeberha '' pochodząca od nazwy przepływającej przez nie rzeki Baakens w języku xhosa , zastępując po ponad 200 latach dotychczasowe `` Port Elizabeth '' .
