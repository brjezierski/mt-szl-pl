Acapulco
Acapulco de Juárez – miasto , port i kurort w Meksyku ( stan Guerrero ) nad Oceanem Spokojnym .
Liczba mieszkańców : 673 tys .
( 2010 ) .
Zespół miejski około 1,1 mln mieszkańców .
Acapulco jest głównym portem w stanie Guerrero , u wybrzeży Oceanu Spokojnego w Meksyku .
Leży 300 km na południowy zachód od stolicy kraju miasta Meksyk .
Usytuowane jest w półkolistej , głębokiej zatoce .
Zbudowane jest na wąskim pasie niskiego lądu .
Ma on 800 metrów szerokości pomiędzy linią brzegową a górami otaczającymi zatokę .
Uważane jest za jeden z najlepszych i najważniejszych portów na tym wybrzeżu Pacyfiku .
Tu przecinają się szlaki handlowe biegnące między Panamą a Kalifornią w USA .
Znajduje się tu także międzynarodowy port lotniczy .
Jednym z najbardziej znanych zabytków miasta jest hiszpański fort San Diego .
Klimat .
Klimat gorący i wilgotny z częstymi opadami w okresie letnim , natomiast w miesiącach zimowych opady znikome bądź niewystępujące .
Historia .
W 1531 roku w pobliże dzisiejszego portu dotarł Hernán Cortés .
W 1550 roku założono osadę .
Prawa miejskie zostały nadane w 1599 roku .
Od XVII wieku Acapulco było głównym węzłem transportowym tej części świata .
Sport .
Od 1993 roku w Acapulco odbywa się turniej tenisa ziemnego Abierto Mexicano Telcel .
