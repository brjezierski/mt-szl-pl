Port Lincoln
Port Lincoln – miasto w Australii Południowej , położone w południowej części Półwyspu Eyrego , nad zatoką Boston Bay .
Boston Bay jest ponad 3 razy większa od Sydney Harbour i uważana jest za jeden z najlepszych naturalnych portów na świecie .
Port Lincoln położony jest około 280 km w linii prostej od Adelaide i około 670 km drogą .
Liczy około 14 000 mieszkańców , roczne opady deszczu wynoszą 488 mm .
Miasto cieszy się typowym klimatem śródziemnomorskim .
