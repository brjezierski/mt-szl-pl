Port Macquarie
Port Macquarie – miejscowość w hrabstwie Macquarie w stanie Nowa Południowa Walia w Australii .
Położone w odległości ok. 390 km od Sydney .
Miasto portowe i turystyczne przy ujściu rzeki Hastings nad Pacyfikiem .
Historia .
Pierwszym Europejczykiem , który stanął w Port Macquarie był John Oxley w roku 1818 .
Miejsce , w roku 1821 ustanowiono kolonią karną i wybudowano więzienie .
W 1823 rozporządzeniem gubernatora Lachlana Macquarie , przeniesiono ok. 1000 więźniów , głównie niepoprawnych i kalekich z więzienia w Newcastle .
Dla osadników Port Macquarie i region , został udostępniony w 1830 roku .
