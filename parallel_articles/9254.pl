Port Moresby
Port Moresby – stolica Papui-Nowej Gwinei na południowo-wschodnim wybrzeżu Nowej Gwinei , nad Zatoką Papua .
Port Moresby wydzielone jest jako odrębny dystrykt stołeczny ( ang .
`` National Capital District '' ) , jest też stolicą Prowincji Centralnej .
Liczba mieszkańców : 366 862 .
W mieście zlokalizowany jest międzynarodowy port lotniczy ( port lotniczy Port Moresby ) i morski oraz uniwersytet ( `` University of Papua New Guinea '' ) .
Zatokę w 1873 odkrył kapitan John Moresby .
Miasto zostało założone w 1883 przez Europejczyków .
W czasie II wojny światowej była to ważna baza wojsk alianckich w walce z Japonią .
W mieście rozwinął się przemysł spożywczy , drzewny oraz maszynowy .
