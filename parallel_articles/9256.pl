Port Vila
Port Vila ( również jako Vila ) – największe miasto i stolica Vanuatu ( szacunkowa liczba mieszkańców w 2010 r. – 47 510 mieszkańców ) , położone na wyspie Efate , w środkowej części archipelagu Nowych Hebrydów .
Stolica prowincji Shefa .
Port Vila stanowi centrum gospodarcze i komunikacyjne kraju .
Znajduje się tutaj główny port morski i port lotniczy .
Podczas II wojny światowej znajdowała się tutaj amerykańska i australijska baza lotnicza .
W 1987 roku miasto zostało poważnie zniszczone przez cyklon tropikalny .
13 marca 2015 roku w wyspy uderzył cyklon tropikalny piątej kategorii o nazwie Pam .
Zniszczenia dotknęły ok. 90 proc .
budynków w stolicy kraju .
