Port-au-Prince ( 1790 )
Port–au–Prince – żaglowiec , początkowo okręt francuski , następnie brytyjski okręt korsarski .
Dzieje .
Okręt wszedł do służby przed 1805 r. Swoją służbę pod banderą francuską zakończył w Port-au-Prince na Haiti .
Tam został zdobyty przez Brytyjczyków i wysłany do Londynu .
Na początku 1805 roku lokalny armator Robert Bent wykupił okręt od Royal Navy .
Zdecydował się też – licząc na spore zyski – uczynić z niego okręt korsarski .
Bent skaptował kapitana Ducka , który wraz z 95 żeglarzami , 12 lutego 1805 roku wyruszył z Gravesend w kierunku hiszpańskich posiadłości w Nowym Świecie .
Duck i jego załoga mieli przede wszystkim napadać na Hiszpanów i przejąć jak najwięcej bogactw ; w razie niemożności realizacji tego celu , mieli rozpocząć połów wielorybów na Pacyfiku .
Port–au–Prince pomyślnie zrealizował główne zadanie , zdobywając cenne przedmioty ze złota i srebra na kilku hiszpańskich jednostkach .
Kpt .
Duck jednak postanowił jeszcze wyruszyć na połów wielorybniczy .
29 listopada 1806 roku wypełniony kosztownościami okręt , rzucił kotwicę u wybrzeża wyspy Lifuka ( Archipelag Haʻapai , Tonga ) .
Wiadomo , że 19 członków załogi zdezerterowało przed dotarciem na wyspę .
Z nieznanych powodów , lokalny władca Finau Ulukalali II , zaatakował Brytyjczyków .
Zaskoczeni korsarze ponieśli klęskę : kpt .
Duck i trzydziestu pięciu członków załogi zginęło .
Statek został zajęty przez wojowników , ograbiony z co cenniejszych rzeczy ( dla Polinezyjczyków ) : dział , prochu i amunicji .
Skarb zaś zatopiono wraz ze statkiem , gdyż dla tubylców nie przedstawiał żadnej wartości .
Do Europy udało się powrócić po czterech latach m.in .
Williamowi Marinerowi , który opisał wyprawę w swoim dzienniku , a także wydał wraz z Johnem Martinem książkę `` Account of the natives of the Tonga Islands '' ( `` Relacja o tubylcach z Wysp Tonga '' ) .
W sierpniu 2012 roku grupa nurków odkryła wrak , który prawdopodobnie jest pozostałością po Port–au–Prince .
