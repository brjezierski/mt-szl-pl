Port-de-Paix
Port-de-Paix ( hait .
`` Pòdepè '' lub `` Pòdpè '' ) – miasto w północno-zachodniej części Haiti , stolica departamentu Nord-Ouest , na wybrzeżu atlantyckim .
W 2007 roku ludność miasta wynosiła 125 000 mieszkańców .
Niegdyś miasto było regionalnym ośrodkiem rolnictwa i przemysłu spożywczego , dziś żyje głównie dzięki przemytowi z USA .
Istnieją regularne połączenia promowe miasta z wyspą Tortuga , położoną nieopodal .
