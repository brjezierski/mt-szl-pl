Porthleven
Porthleven – miasto w Anglii w Kornwalii , położone w pobliżu Helston na półwyspie Lizard nad zatoką Mount ’ s Bay .
Jest najbardziej wysuniętym na południe portem Wielkiej Brytanii .
W 2001 roku miasto zamieszkiwało 3190 osób .
Historia .
Do XVIII w. Porthleven był małą wsią , której mieszkańcy zajmowali się rybołówstwem i kopalnictwem cyny .
Po kilku tragediach morskich , a w szczególności po rozbiciu się statku HMS Anson w The Loe w r. 1807 , parlament uchwalił akt , na mocy którego miano zbudować bezpieczny port w Porthleven .
Budowa stoczni trwała 14 lat i ukończona została w 1825 r .
