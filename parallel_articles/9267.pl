Porto Alegre
Porto Alegre ( wym . )
– miasto w południowej Brazylii liczące razem z przedmieściami ponad 3 miliony mieszkańców .
Geografia .
Stolica stanu Rio Grande do Sul , port morski nad Lagoa dos Patos .
Historia i kultura .
Choć jego początki sięgają XVIII wieku , to miasto zostało oficjalnie założone w 1809 .
Stanowi ważne centrum dla kultury `` gaúcho '' ; tak też zwykle określa się mieszkańców stanu .
Znane jest m.in .
