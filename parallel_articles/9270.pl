Porto Velho
Porto Velho – miasto w Brazylii , w Regionie Północnym , stolica stanu Rondônia .
Jest położona nad rzeką Madeira , jednym z głównych dopływów Amazonki .
Siedziba rzymskokatolickiego arcybiskupa Porto Velho .
W Porto Velho urodził się Moacyr Grechi , brazylijski duchowny katolicki , arcybiskup Porto Velho
Transport .
Siedem kilometrów od miasta znajduje się Port lotniczy Porto Velho , który obsłużył w 2010 roku 716 905 pasażerów .
Odbywają się stamtąd loty głównie do innych miast brazylijskich .
