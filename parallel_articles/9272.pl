Geografia .
Portoryko należy do geologicznej struktury Wielkich Antyli .
Obszar wyspy zajmują sfałdowane w czasie orogenezy alpejskiej Cordillera Central z najwyższym szczytem kraju – Corro de Punta ( 1338 m n.p.m .
) , oprócz tego pasma na wyspie rozciągają się Sierra de Luqillo i Sierra de Cayey .
Portoryko leży w strefie klimatu podrównikowego wilgotnego , który kształtuje północno-wschodni pasat .
Średnia roczna temperatura wynosi 26 °C , a opady są zróżnicowane i wynoszą od około 1000 mm do nawet 4000 mm i są nierównomiernie rozłożone w ciągu roku .
Roślinność wyspy jest w znacznym stopniu wyniszczona , lasy tropikalne zajmują niewielki obszar wyspy .
Fauna należy do antylskiej krainy netropikalnej i jest bogata pod względem gatunkowym .
