Portrait of an American Family
Portrait of an American Family – debiutancki album amerykańskiej grupy Marilyn Manson .
Został wydany 19 lipca 1994 roku i wyprodukowany przez Trenta Reznora i Nine Inch Nails .
Początkowo album był zatytułowany `` The Manson Family Album '' ( jako odniesienie do Charlesa Mansona i jego sekty ) , lecz ostatecznie zmieniono tytuł na `` Portrait of an American Family '' .
Jest to jedyny album grupy który został nagrany z basistą Gidgetem Geinem , który został zwolniony i zastąpiony przez Twiggy Ramireza .
Zwolnienie było argumentowane jego uzależnieniem od heroiny i dotarciem tej informacji do mediów .
Album sprzedał się w nakładzie 2.5 miliona kopii .
Wydano trzy single : Get Your Gunn , Lunchbox i Dope Hat .
