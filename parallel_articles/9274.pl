Portslade
Portslade – osada w Anglii , w East Sussex , w dystrykcie ( unitary authority ) Brighton and Hove .
Leży 6,3 km od miasta Brighton , 16,5 km od miasta Lewes i 75,1 km od Londynu .
W 2001 miejscowość liczyła 19 564 mieszkańców .
Portslade jest wspomniana w Domesday Book ( 1086 ) jako `` Porteslage '' / '' Porteslamhe '' .
