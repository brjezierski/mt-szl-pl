Portsmouth ( Ohio )
Portsmouth – miasto w Stanach Zjednoczonych , w południowej części stanu Ohio , nad rzeką Ohio .
Liczba mieszkańców w 2000 r. wynosiła 20 917 .
W mieście rozwinął się przemysł chemiczny , skórzany oraz hutniczy .
Klimat .
Miasto leży w strefie klimatu umiarkowanego , subtropikalnego , z gorącym latem , o opadach równomiernie rozłożonych w roku , należącego według klasyfikacji Köppena do strefy `` Cfa '' .
Średnia temperatura roczna wynosi 13,0°C , a opady 1049 mm ( w tym do 34,5 cm opadów śniegu ) .
Średnia temperatura najcieplejszego miesiąca - lipca wynosi 24,6°C , najzimniejszego - stycznia 0,8°C , podczas gdy rekordowe temperatury wynoszą odpowiednio 41,7°C i -33,9°C .
