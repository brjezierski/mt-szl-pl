Portugalia
Portugalia , Republika Portugalska ( port .
`` Portugal '' , wym .
[ ] ; oficjalna nazwa `` República Portuguesa '' ) – państwo europejskie położone w zachodniej części Europy Południowej na południowym zachodzie Półwyspu Iberyjskiego .
Jest najdalej wysuniętym na zachód państwem Europy , od północy i wschodu graniczy z Hiszpanią , a od zachodu i południa Portugalię oblewają wody Oceanu Atlantyckiego .
Dodatkowo w skład Portugalii wchodzą dwa archipelagi wysp położonych na Oceanie Atlantyckim – Azory i Madera .
Jest członkiem Unii Europejskiej oraz NATO .
