Porvoo ( 1991 )
Porvoo ( 72 ) – fiński kuter rakietowy z końca XX wieku , jedna z czterech jednostek typu `` Rauma '' .
Nazwa pochodzi od miasta Porvoo , położonego w południowej części kraju i jednego z najstarszych w Finlandii .
Okręt został zwodowany 8 listopada 1991 roku w stoczni Finnyards w Raumie , a do służby w Fińskiej Marynarce Wojennej wszedł 27 kwietnia 1992 roku .
Jednostka nadal znajduje się w składzie floty i ma status operacyjny ( stan na 2019 rok ) .
Projekt i budowa .
Projekt okrętów typu `` Rauma '' powstał w lutym 1987 roku na bazie doświadczeń z eksploatacji kutrów rakietowych typu `` Helsinki '' , wcielonych do służby w latach 1981–1986 .
Okręty w porównaniu do pierwowzoru są dłuższe , węższe i mają nieco mniejszą wyporność , lecz zabierają zbliżony zestaw uzbrojenia .
„ Porvoo ” został zamówiony i zbudowany w stoczni Finnyards w Raumie 27 sierpnia 1987 roku , wraz z trzema bliźniaczymi jednostkami .
Numer stoczniowy okrętu to 273 .
Ceremonia wodowania odbyła się 8 listopada 1991 roku .
Dane taktyczno-techniczne .
