Afryka Środkowa
Afryka Środkowa − region Afryki , obejmujący centralną część kontynentu .
Afryka Środkowa to termin używany m.in .
przez Organizację Narodów Zjednoczonych w jej geoschemacie dla Afryki i obejmuje następujące kraje : Angolę , Burundi , Czad , Demokratyczną Republikę Konga , Gabon , Gwineę Równikową , Kamerun , Kongo , Republikę Środkowoafrykańską , Wyspy Świętego Tomasza i Książęcą , Zambię .
Sześć spośród tych krajów ( Kamerun , Republika Środkowoafrykańska , Czad , Gwinea Równikowa , Gabon i Republika Konga ) jest również członkami Wspólnoty Gospodarczej i Walutowej Afryki Środkowej ( CEMAC ) i dzieli wspólną walutę – franka CFA Afryki Środkowej .
Z kolei Afrykański Bank Rozwoju definiuje Afrykę Środkową jako siedem krajów : Kamerun , Republika Środkowoafrykańska , Czad , Republika Konga , Demokratyczna Republika Konga , Gwinea Równikowa i Gabon .
Skład etniczny .
Po migracji Bantu Afryka Środkowa jest zamieszkana głównie przez ludy Bantu i przeważają tutaj języki bantu .
Należą do nich m.in .
ludy Mongo , Kongo i Luba .
Afryka Środkowa obejmuje również wiele ludów ubangijskich , nilo-saharyjskich i nigero-kongijskich : w północnej części Afryki Środkowej dominują Kanuri z grupy nilo-saharyjskiej .
