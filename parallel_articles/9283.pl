Ameryka Środkowa
Ameryka Środkowa – centralny region geograficzny Ameryki .
Jest definiowany jako południowa część Ameryki Północnej , w której skład wchodzą dwie części : kontynentalna ( Ameryka Centralna ) , rozciągająca się między przesmykiem Tehuantepec a Przesmykiem Panamskim , i wyspiarska ( Karaiby ) .
Obszar liczy około 850 000 km² powierzchni i jest zamieszkany przez ponad 90 mln ludzi .
Państwa Ameryki Środkowej .
Dodatkowo w Ameryce Środkowej znajduje się kilkanaście terytoriów zależnych .
