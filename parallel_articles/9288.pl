Potebniamyces pyri
Potebniamyces pyri ( Berk .
& amp ; Broome ) Dennis – gatunek grzybów z rodziny `` Phacidiaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Phacidiaceae , Phacidiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisali go w 1876 r. Miles Joseph Berkeley i Christopher Edmund Broome , nadając mu nazwę `` Stictis lecanora '' `` var .
pyri '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Richard William George Dennis w 1978 r .
Synonimy naukowe :
Znana jest głównie anamorfa `` Phacidiopycnis pyri '' .
Teleomorfa jest rzadka .
Charakterystyka .
