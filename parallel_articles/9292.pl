Poczdam
Poczdam ( ) – miasto we wschodnich Niemczech , położone nad Hawelą .
Stolica i największe miasto kraju związkowego Brandenburgia , wchodzące w skład obszaru metropolitalnego Berlin-Brandenburg .
Miasto znane jest głównie ze swoich bardzo cennych zespołów zabytkowych , szczególnie z zespołu parkowo-pałacowego Sanssouci , wybudowanego w XVIII wieku przez Fryderyka II Wielkiego , który od 1990 znajduje się na liście światowego dziedzictwa UNESCO .
Od połowy XIX wieku Poczdam rozwijał się jako centrum kulturalno-naukowe .
Obecnie miasto jest jednym ze znaczących ośrodków akademickich we wschodnich Niemczech .
Działają tu trzy uczelnie publiczne oraz ponad 30 instytutów badawczych .
