W skład jednostki weszły obszary dotychczasowych gromad Rdzów , Potworów i Łojków ze zniesionej gminy Potworów w tymże powiecie .
Dla gromady ustalono 17 członków gromadzkiej rady narodowej .
1 stycznia 1956 gromada weszła w skład nowo utworzonego powiatu przysuskiego w tymże województwie .
31 grudnia 1959 do gromady Potworów przyłączono obszar zniesionej gromady Grabowa .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
1 stycznia 1973 w powiecie przysuskim reaktywowano gminę Potworów .
