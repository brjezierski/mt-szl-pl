Pouancé
Pouancé – miejscowość i dawna gmina we Francji , w regionie Kraj Loary , w departamencie Maine i Loara .
W 2013 roku jej populacja wynosiła 3177 mieszkańców .
W dniu 15 grudnia 2016 roku z połączenia 10 ówczesnych gmin – La Chapelle-Hullin , Chazé-Henry , Combrée , Grugé-l ’ Hôpital , Noëllet , Pouancé , La Prévière , Saint-Michel-et-Chanveaux , Le Tremblay oraz Vergonnes – powstała nowa gmina Ombrée-d ’ Anjou .
Siedzibą gminy została miejscowość Pouancé .
