Wólka Poznańska
Wólka Poznańska – wieś w Polsce położona w województwie lubelskim , w powiecie łukowskim , w gminie Stoczek Łukowski .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii Wniebowzięcia Najświętszej Maryi Panny w Stoczku Łukowskim .
