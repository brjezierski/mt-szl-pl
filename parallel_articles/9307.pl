Pozzuoli ( stacja kolejowa )
Pozzuoli ( wł .
`` Stazione di Pozzuoli '' ) – stacja kolejowa w Pozzuoli , w prowincji Neapol , w regionie Kampania , we Włoszech .
Położona jest na linii Cumana , w historycznym centrum Pozzuoli .
Historia .
Stacja została otwarta 15 grudnia 1889 wraz z odcinkiem Terme Patamia–Pozzuoli linii Cumana , ukończonej w całości w roku następnym .
