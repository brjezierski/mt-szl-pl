Gepard grzywiasty
Gepard grzywiasty , gepard ( `` Acinonyx jubatus '' ) – gatunek drapieżnego ssaka z podrodziny kotów ( Felinae ) w obrębie rodziny kotowatych ( Felidae ) .
Jest jedynym żyjącym przedstawicielem rodzaju `` Acinonyx ''
Polska nazwa „ gepard ” pochodzi ze średniowiecznej łaciny : `` gattus pardus '' , co znaczyło „ kot-leopard ” .
Taksonomia .
Gatunek po raz pierwszy zgodnie z zasadami nazewnictwa binominalnego opisał w 1775 roku niemiecki przyrodnik Johann Christian Daniel von Schreber nadając mu nazwę `` Felis jubata '' .
Holotyp pochodził z Prowincji Przylądkowej Zachodniej , w Południowej Afryce .
Jedyny żyjący współcześnie przedstawiciel rodzaju gepard ( `` Acinonyx '' ) .
Do niedawna gepard umieszczany był w monofiletycznej podrodzinie Acinonychinae , obecnie na podstawie dowodów genetycznych i klastrów tworzy klad wraz z `` Puma concolor '' i `` Puma yagouaroundi '' , których drogi rozwojowe rozeszły się 6,9 mln lat temu .
