Połchowo ( Białoruś )
Połchowo ( ; ) – wieś na Białorusi , w obwodzie brzeskim , w rejonie pińskim , w sielsowiecie Łopacin .
W dwudziestoleciu międzywojennym leżało w Polsce , w województwie poleskim , w powiecie pińskim , w gminie Lemieszewicze .
Po II wojnie światowej w granicach Związku Sowieckiego .
Od 1991 w niepodległej Białorusi .
