Połczyn-Zdrój ( gmina )
Połczyn-Zdrój – gmina miejsko-wiejska w północno-zachodniej Polsce , położona jest w środkowej części województwa zachodniopomorskiego , w powiecie świdwińskim , położona na Pojezierzu Drawskim .
Siedzibą gminy jest miasto Połczyn-Zdrój .
Według danych z 31 grudnia 2016 r. gmina miała 15 531 mieszkańców .
Miejsce w województwie ( na 114 gmin ) :
powierzchnia 12. , ludność 21 .
Położenie .
Gmina znajduje się w środkowej części województwa zachodniopomorskiego , we wschodniej części powiatu świdwińskiego .
Do 31 grudnia 1998 r. wchodziła w skład województwa koszalińskiego .
Sąsiednie gminy :
Według danych z 1 stycznia 2009 powierzchnia gminy wynosi 344,30 km² .
Gmina stanowi 31,4 % powierzchni powiatu .
Gmina jest położona na Pojezierzu Drawskim .
Południowo-wschodnia część znajduje się w granicach Drawskiego Parku Krajobrazowego , na terenie którego znajduje się rezerwat przyrody Dolina Pięciu Jezior .
Na zachód od parku inny rezerwat , Torfowisko Toporzyk .
