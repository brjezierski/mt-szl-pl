Ocean Południowy
Ocean Południowy ( także : Południowy Ocean Lodowaty , Ocean Antarktyczny ) – ocean rozciągający się od wybrzeża Antarktydy do równoleżnika 60°S , wzdłuż którego łączy się z oceanami : Spokojnym , Indyjskim i Atlantyckim .
Ocean Południowy jest czwartym co do wielkości spośród wszystkich pięciu .
Jego obszar został formalnie zatwierdzony przez Międzynarodową Organizację Hydrograficzną w 2000 roku .
Oficjalna polska nazwa tego oceanu , zatwierdzona przez Komisję Standaryzacji Nazw Geograficznych brzmi Ocean Południowy .
Położenie i obszar .
Według ustaleń Międzynarodowej Organizacji Hydrograficznej , nazwa „ Ocean Południowy ” odnosi się do wód Oceanu Światowego otaczających Antarktydę , których północną granicą jest równoleżnik 60°S .
W jego skład wchodzą mniejsze akweny – m.in .
Morze Amundsena , Morze Bellingshausena , fragment Cieśniny Drake ’ a , Morze Rossa i Morze Weddella .
Powierzchnia tego oceanu to 20,327 milionów km² .
Linia brzegowa ma długość 17 968 km .
Nazwa ta nie jest zawsze stosowana w ścisłym rozumieniu .
W Australii do zasięgu opisanego powyżej dodaje się obszar między równoleżnikiem 60°S i południowymi wybrzeżami Australii oraz Nowej Zelandii .
Na mapach wybrzeży Tasmanii i południowej Australii zawsze widnieje nazwa „ Ocean Południowy ” , nigdy „ Ocean Indyjski ” .
Naturalną granicą obszaru wód wokół Antarktydy nie jest jeden równoleżnik , ale strefa konwergencji antarktycznej , gdzie wody płynące od wybrzeży kontynentu zderzają się z cieplejszymi prądami zmierzającymi na południe .
