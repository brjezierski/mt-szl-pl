Karolina Południowa
Karolina Południowa ( ang .
`` South Carolina '' ) ( ) – stan w południowo-wschodniej części Stanów Zjednoczonych , graniczący od północy z Karoliną Północną , od południowego zachodu z Georgią .
Granicę wschodnią stanowi Ocean Atlantycki .
Karolina Południowa jako ósmy stan ratyfikowała konstytucję USA w 1788 roku .
Do 1860 roku 10 % z 4 milionów amerykańskich niewolników mieszkało w Karolinie Południowej .
Stan znany jest ze swojej roli w wojnie secesyjnej i jako pierwszy odłączył się od Unii w grudniu 1860 roku .
Obszar metropolitalny Greenville–Spartanburg–Anderson jest największy w stanie i obejmuje 1,5 mln mieszkańców .
Geografia .
Karolina Południowa znajduje się na wschodnim wybrzeżu USA w połowie drogi między Nowym Jorkiem a Miami .
Na północy graniczy z Karoliną Północną , na południu i zachodzie ze stanem Georgia ( granica biegnie wzdłuż rzeki Savannah ) .
Granicę wschodnią stanowi Ocean Atlantycki , którego wybrzeże liczy 297,7 km ( 185 mil ) .
Administracyjnie jest podzielona na 46 hrabstw .
Największe miasta to Columbia ( stolica ) , Charleston , Mount Pleasant i Rock Hill .
Ukształtowanie powierzchni .
Około 40 % powierzchni stanu zajmuje nadmorska równina , której wyniesienie nad poziom morza zawiera się w przedziale między 15 a 60 m n.p.m .
Centralna część także jest równinna a tylko północna i zachodnia część ( granica z Georgią i Karoliną Północną ) ma charakter podgórski z fragmentem łańcucha Blue Ridge Range należącego do Appalachów .
Jednak i tutaj wysokość mieści się w przedziale między 300 a 600 m n.p.m .
