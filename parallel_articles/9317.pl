Geografia .
Ukształtowanie powierzchni .
Korea Południowa jest krajem górzysto-wyżynnym .
We wschodniej części , wzdłuż wybrzeża Morza Wschodniego ( `` Morze Japońskie '' ) , ciągną się Góry Wschodniokoreańskie , zbudowane z prekambryjskich skał metamorficznych ( głównie łupki ) i granitów , rozcięte dolinami rzek na kilka pasm górskich : Góry Diamentowe ( Sorak-san , 1708 m ) na granicy z Koreą Północną oraz Gyeongsang i Sobaek ( Cziri-san , 1915 m ) , rozdzielone niziną rzeki Naktong-gang .
W zachodniej części aluwialna nizina nad Morzem Żółtym z izolowanymi wzniesieniami o wysokości 700–800 m. Wybrzeże Morza Wschodniego ( `` Morze Japońskie '' ) wyrównane , Morza Żółtego – riasowe z licznymi zatokami , półwyspami i przybrzeżnymi wyspami , największa Czedżu ( wysokość do 1950 m – wulkan Halla-san , najwyższy szczyt kraju ) w Cieśninie Koreańskiej .
Klimat .
Klimat jest monsunowy , w północnej i środkowej części umiarkowany ciepły , w południowej – podzwrotnikowy .
Średnia temperatura w styczniu od –4 °C na północy do 2–4 °C na południu , w lipcu odpowiednio od 21 °C do 25–27 °C .
Średnia roczna suma opadów od 1000 mm na nizinach do 1500 mm w górach .
Pora opadowa czerwiec–wrzesień ( ciepły , wilgotny monsun znad Oceanu Spokojnego ) .
Późnym latem do wybrzeży Korei Południowej docierają tajfuny .
