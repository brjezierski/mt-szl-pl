Połtawa
Połtawa ( ukr .
Полтава ) – miasto w środkowo-wschodniej części Ukrainy , nad Worsklą .
Siedziba władz obwodu oraz rejonu połtawskiego .
Jest ważnym ośrodkiem kulturalnym i dużym węzłem komunikacyjnym .
Położenie .
Połtawa znajduje się we wschodniej części Europy , 301 km na wschód od Kijowa .
Miasto położone jest na Nizinie Naddnieprzańskiej , na obu brzegach rzeki Worskla .
Jeden z dopływów rzeki , Kołomak , wpada do Worskli na terenie miasta .
W obrębie miasta znajduje się kilka małych naturalnych jezior i mnóstwo stawów .
Rzeźba terenu jest przeważnie jednolita , w odległości 1,5 km od rzeki jest ostry stok ( prawy brzeg Worskli , nad którym leży większa część miasta , jest bardziej stromy , wysokość wzgórz sięga 80–100 m w odniesieniu do poziomu rzeki ) .
Położenie geograficzne Połtawy jest dość korzystne , a z biegiem historii wywarło wielki wpływ na rozwój miasta .
