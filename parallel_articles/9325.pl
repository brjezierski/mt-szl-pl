Praia ( concelho )
Praia - jedno z dwudziestu dwóch concelhos w Republice Zielonego Przylądka .
Położone jest na wyspie Santiago .
Ponad 90 % ludności concelho Praia mieszka w jej stolicy .
W concelho Praia znajduje się tylko jedna parafia , Nossa Senhora da Graça .
Historia .
Concelho Praia jest jedną z najstarszych jednostek administracyjnych w Republice Zielonego Przylądka , powstało bowiem w XVIII wieku .
