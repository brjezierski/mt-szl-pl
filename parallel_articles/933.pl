Acompsomyces stenichni
Acompsomyces stenichni ( Scheloske ) I.I .
Tav .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Acompsomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał w 1969 r. H.-W. Scheloske nadając mu nazwę `` Stigmatomyces stenichni '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu I.I .
Tav w 1985 r .
Charakterystyka .
Grzyb mikroskopijny , pasożyt owadów .
W Polsce jego występowanie podał Tomasz Majewski na chrząszczu `` Stenichnus collaris '' .
