Czerwienica prawdziwa
Czerwienica prawdziwa , choroba Vaqueza ( łac .
`` polycythemia vera '' ) – pierwotna choroba mieloproliferacyjna , przebiegająca ze zwiększeniem liczby erytrocytów , granulocytów i płytek krwi , z przewagą erytropoezy .
Epidemiologia .
Występuje z częstością 2:100 000 , częściej u mężczyzn po 50. roku życia .
Etiopatogeneza .
Nie do końca poznana .
Czasem może przechodzić w przewlekłą białaczkę szpikową .
