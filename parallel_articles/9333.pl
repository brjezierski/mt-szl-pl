Prekmurje
Prekmurje ( także `` Prekomurje '' , , , prek . )
– niewielki region historyczny w dzisiejszej Słowenii .
Prekmurje ma powierzchnię 1093 km² .
Zajmuje skrajny północno-wschodni zakątek Słowenii , wciśnięty między granice z Austrią , Węgrami i Chorwacją .
Od reszty Słowenii oddziela je rzeka Mura .
Na południu sąsiaduje z chorwackim regionem Međimurje o podobnej historii .
Północ Prekmurja zajmuje pagórkowate przedgórze Alp `` Goričko '' ( najwyższy szczyt `` Dreiländereck '' na granicy z Węgrami i Austrią – 387 m n.p.m .
) , południową część stanowią doliny rzek Ledavy i Mury .
Prekmurje obecnie nie stanowi oddzielnej jednostki administracyjnej .
Stanowi część okręgu statystycznego `` Pomurska statistična regija '' , dzieli się na jednostki administracji rządowej Murska Sobota i Lendava oraz na samorządne gminy Beltinci , Cankova , Črenšovci , Dobrovnik , Gornji Petrovci , Grad , Hodoš , Kobilje , Kuzma , Lendava , Moravske Toplice , Murska Sobota ( jedyna miejska gmina w regionie ) , Odranci , Puconci , Rogašovci , Šalovci , Tišina , Turnišče i Velika Polana .
Jest to region rolniczy , pozbawiony większych ośrodków miejskich .
Jedynym miastem i głównym ośrodkiem regionu jest Murska Sobota ( 20,0 tys .
mieszkańców w 2005 ) , drugim co do wielkości – wieś Lendava ( ok. 4 tys .
mieszkańców ) .
W Lendavie mieści się rafineria naftowa .
Przez Prekmurje ma przebiegać projektowana autostrada A5 z Mariboru do przejścia granicznego Pince – Tornyiszentmiklós na granicy z Węgrami .
Prekmurje zamieszkuje 82 369 osób ( 2002 ) .
W przeważającej większości są to Słoweńcy , posługujący się specyficznym dialektem języka słoweńskiego .
Charakterystyczna dla tego regionu jest spora liczba Romów .
W 30 wsiach w przygranicznych gminach Hodoš ( węg .
`` Hodos '' ) , Moravske Toplice , Šalovci , Lendava ( `` Lendva '' ) i Dobrovnik ( `` Dobrónak '' ) mieszka 7–8 tys .
autochtonicznych Węgrów .
Oprócz tego region zamieszkuje czasowo i dojeżdża to do pracy liczna grupa Chorwatów z sąsiedniego Međimurja , którzy mają tu lepsze warunki życia .
Po czasach Ilirów , Celtów , Rzymian , Gotów i Longobardów , od najwcześniejszego średniowiecza Prekmurje było zasiedlone przez Słowian południowych .
Od XI wieku Prekmurje było częścią historycznego Królestwa Węgier , w którym było rozdzielone między komitaty Vas i Zala .
W XIX wieku wśród tutejszych autochtonicznych Słoweńców ( „ węgierskich ” ) pojawiła się świadomość wspólnoty narodowej ze Słoweńcami austriackimi .
Na przełomie XIX i XX wieku świadomość narodowa wzmogła się wskutek intensywnej madziaryzacji .
Po I wojnie światowej na fali rewolucji powstała na tym terenie efemeryczna bolszewicka Republika Murska .
Proklamowana 29 maja 1919 , została zlikwidowana 3 czerwca 1919 przez Węgierską Armię Czerwoną .
Za zezwoleniem konferencji wersalskiej 12 sierpnia 1919 Prekmurje zajęły wojska serbskie .
Na mocy traktatu w Trianon 4 czerwca 1920 Prekmurje zostało oderwane od Węgier i przyłączone do ówczesnego Królestwa Serbów , Chorwatów i Słoweńców .
Węgry okupowały ten teren w czasie hitlerowskiej agresji na Jugosławię w 1941 .
16 grudnia 1941 węgierski parlament uchwalił włączenie Prekmurja do Węgier .
Na początku 1945 władze węgierskie zorganizowały przymusowy wywóz młodzieży słoweńskiej na roboty do Niemiec i na Węgry , co spotęgowało wśród ludności wrogość wobec Węgier .
Region został zajęty w kwietniu 1945 przez Armię Czerwoną .
