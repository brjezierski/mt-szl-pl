Dialekt prekmurski
Dialekt prekmurski ( , , „ język wendyjski ” , samonazwanie : `` prekmürščina '' ) – peryferyjny dialekt języka słoweńskiego należący do grupy dialektów panońskich , używany w regionie Prekmurje , w Słowenii .
Jest to najdalej wysunięty na północny wschód dialekt słoweński .
Liczbę użytkowników szacuje się na około 80 tys .
