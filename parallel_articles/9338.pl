Presidente Prudente
Presidente Prudente – miasto w południowo-wschodniej Brazylii , w stanie São Paulo , nad rzeką Santo Anastácio .
W 2010 roku liczba mieszkańców miasta wynosiła 207 625 .
Ośrodek handlowy regionu uprawy bawełny ; rozwinięty przemysł mleczarski , chemiczny , włókienniczy i drzewny .
Miasto zostało założone 14 września 1917 roku .
