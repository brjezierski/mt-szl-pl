Acquaviva
Acquaviva ( wł .
`` Castello di Acquaviva '' ) – jeden z 9 zamków w San Marino .
W 2012 roku liczył 2048 mieszkańców .
Swoją nazwę zawdzięcza ważnemu źródłu u stóp Góry Montecerreto , pokrytej lasem sosnowym .
Na górze tej wznosił się stary dwór Stirvano , gdzie został spisany werdykt Feretrano ( Placito Feretrano ) w roku 885 .
Legenda głosi , iż pierwszym schronieniem Św .
Marino była grota w urwisku skalnym o nazwie Baldasserona .
Na jej terytorium znajduje się tor wyścigowy dla profesjonalnych zawodników motocrossu .
W dolinie nad brzegami rzeki San Marino znajduje się centrum przemysłowe Gualdicciolo .
