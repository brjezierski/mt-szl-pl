Prestatyn
Prestatyn ( ) – miasto w hrabstwie Denbighshire w Wielkiej Brytanii ( północna Walia ) .
W 2001 roku liczyło 18 496 mieszkańców .
Głównie ośrodek turystyczny .
Miasto to jest dobrze znane przez fanów snookera .
Tutaj bowiem odbywa się bardzo wiele kwalifikacji do turniejów rozgrywanych w Main Tourze .
