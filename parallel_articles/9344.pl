Lista prezydentów Polski
Prezydenci Polski – chronologiczna lista prezydentów Polski i innych osób sprawujących urząd głowy państwa polskiego od 1918 roku .
Długość urzędowania prezydentów .
Najdłużej urzędującym prezydentem Polski był August Zaleski , pełniący tę funkcję przez 9069 dni .
W okresie międzywojennym najdłużej urzędującym prezydentem był Ignacy Mościcki ( 4866 dni ) , wśród głów państwa PRL Henryk Jabłoński ( 4971 dni ) , a w III RP Aleksander Kwaśniewski ( 3653 dni ) .
Najkrócej urzędującym prezydentem Polski był Gabriel Narutowicz , sprawujący urząd przez 2 dni .
Wśród prezydentów na uchodźstwie najkrócej urzędującym prezydentem był Ryszard Kaczorowski ( 521 dni ) , wśród głów państwa PRL Józef Cyrankiewicz ( 461 dni ) , a w III RP Wojciech Jaruzelski ( 356 dni ) .
Wiek prezydentów .
Najmłodszym prezydentem Polski był Aleksander Kwaśniewski .
