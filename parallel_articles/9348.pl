Primark
Primark Stores Limited – irlandzkie przedsiębiorstwo odzieżowe zajmujące się sprzedażą detaliczną odzieży , należące do koncernu Associated British Foods .
Na rodzimym rynku przedsiębiorstwo operuje pod nazwą Penneys .
Przedsiębiorstwo zostało założone w 1969 roku , a jego siedziba mieści się w Dublinie .
W 2015 roku sieć Primark liczyła ponad 270 sklepów w dziewięciu krajach Europy : w Wielkiej Brytanii ( 161 ) , Irlandii ( 38 ) , Austrii , Belgii , Francji , Hiszpanii , Holandii , Niemczech i Portugalii , a także w Stanach Zjednoczonych .
W kwietniu 2016 otwarto pierwszy sklep we Włoszech .
W sierpniu 2020 roku spółka otworzyła swój pierwszy sklep w Polsce , w Galerii Młociny w Warszawie .
W maju 2021 roku otwarto drugi w Galerii Posnania w Poznaniu .
