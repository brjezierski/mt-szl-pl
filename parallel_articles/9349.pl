Prince Albert
Prince Albert – trzecie co do wielkości miasto prowincji Saskatchewan w Kanadzie .
Usytuowane w centralnej części prowincji nad brzegami rzeki North Saskatchewan .
Nazywane `` Bramą Północy '' gdyż jest ostatnim znaczącym skupiskiem osiedli ludzkich na drodze ku dalekiej północy .
Według spisu ludności z 2005 roku miasto to liczyło ok. 35 tys .
