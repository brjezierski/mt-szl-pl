Pringles
Pringles – marka chipsów ziemniaczanych należąca do Kellogg Company .
Ich cechą charakterystyczną jest pakowanie w tekturowe tuby .
Historia .
Marka została założona w 1968 r. Na początku sprzedawano je tylko w Stanach Zjednoczonych , a od lat 70 .
XX wieku są rozsyłane po całym świecie .
Jest kilka teorii tłumaczących , dlaczego firma nazywa się `` Pringles '' .
Jedna z nich odwołuje się do postaci Marka Pringlesa , który stworzył patent 2 286 644 o nazwie `` Metody i sprzęt do obróbki ziemniaków '' .
Stało się to 5 marca 1937 r. Jego pracę wykorzystało przedsiębiorstwo Procter & amp ; Gamble ( P & amp ; G ) , używając również swojego własnego patentu .
