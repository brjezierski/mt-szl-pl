Priti Patel
Priti Patel ( ur .
29 marca 1972 w Londynie ) − brytyjska polityk , członek Partii Konserwatywnej .
Od 2010 poseł do Izby Gmin .
W pierwszym i drugim gabinecie Theresy May minister rozwoju międzynarodowego Wielkiej Brytanii .
W pierwszym gabinecie Borisa Johnsona zajmowała stanowisko ministra spraw wewnętrznych , utrzymała je po .
Pochodzi z rodziny o korzeniach ugandyjsko-hinduskich .
Ukończyła studia ekonomiczne na Keele University a następnie kierunek polityka brytyjska na University of Essex .
Znana z eurosceptycyzmu , była jedną głównych postaci ruchu podczas kampanii przed referendum w sprawie dalszego członkostwa Wielkiej Brytanii w Unii Europejskiej .
Podczas pełnienia przez nią funkcji ministra spraw wewnętrznych wprowadzono system punktowy dla osób chętnych do imigracji do Wielkiej Brytanii , w którym brane jest pod uwagę wykształcenie , zapotrzebowanie brytyjskiego rynku pracy na dany zawód oraz poziom znajomości języka angielskiego .
Jest mężatką , ma syna .
