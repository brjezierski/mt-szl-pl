Prohn
Prohn – gmina w Niemczech , wchodząca w skład urzędu Altenpleen w powiecie Vorpommern-Rügen , w kraju związkowym Meklemburgia-Pomorze Przednie .
Toponimia .
Nazwa pochodzenia słowiańskiego , być może od imienia boga Peruna , co sugerowałoby istnienie ośrodka jego kultu w tym miejscu .
Rekonstruowana jako `` Piorun '' .
