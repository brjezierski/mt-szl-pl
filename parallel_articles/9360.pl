Prostokąt
Prostokąt – czworokąt , który ma wszystkie wewnętrzne kąty proste ( stąd również jego nazwa ) .
Prostokąt jest szczególnym przypadkiem trapezu prostokątnego oraz równoległoboku .
Szczególnym przypadkiem prostokąta ( o wszystkich bokach tej samej długości ) jest kwadrat .
Prostokąt , który nie jest kwadratem , ma dokładnie dwie osie symetrii i środek symetrii .
Przekątne prostokąta są równej długości i przecinają się w połowie .
Kąt między przekątnymi jest prosty wtedy i tylko wtedy , gdy prostokąt jest kwadratem .
