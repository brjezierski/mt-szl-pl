Prościejów
Prościejów ( , wym .
; ) – miasto w Czechach , na Morawach , w kraju ołomunieckim .
Położone jest w zachodniej części historycznej krainy Haná , w obrębie zapadliska Obniżenie Górnomorawskie , na wschód od Wyżyny Drahańskiej i na południowy wschód od Wyżyny Zabrzeskiej .
Leży ok. 225 m n.p.m .
Przez miasto przepływają rzeki Hloučela i Romže .
Według danych z 31 grudnia 2003 powierzchnia miasta wynosiła 46,58 km² , a liczba jego mieszkańców – 47 374 osób .
