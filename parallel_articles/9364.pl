Wólka Proszewska
Wólka Proszewska – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Mokobody .
Leży nad rzeką Liwiec .
Wierni Kościoła rzymskokatolickiego należą do parafii Matki Bożej Królowej Korony Polskiej w Kopciach .
We wsi działa założona w 1964 roku jednostka ochotniczej straży pożarnej .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
