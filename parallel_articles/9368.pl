Providence ( komiks )
Providence – amerykańska seria komiksowa autorstwa angielskiego scenarzysty Alana Moore ' a i amerykańskiego rysownika Jacena Burrowsa , wydana w formie 12 zeszytów między majem 2015 a kwietniem 2017 przez Avatar Press .
Nawiązująca do tzw .
mitologii Cthulhu stworzonej przez H.P .
Lovecrafta , `` Providence '' jest jednocześnie prequelem i sequelem do dwóch innych miniserii komiksowych Moore ' a i Burrowsa , `` Podwórze '' ( ang .
`` Alan Moore 's The Courtyard '' ) i `` Neonomicon '' .
