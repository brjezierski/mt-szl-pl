Prowansja
Prowansja ( fr .
`` Provence '' , prow .
`` Provença '' lub `` Prouvènço '' ) – kraina historyczna w południowo-wschodniej Francji , nad Morzem Śródziemnym , na wschód od dolnego biegu Rodanu .
Nazwa wzięta od prowincji rzymskiej Galia Zaalpejska , którą Rzymianie nazywali także `` Provincia Nostra '' – „ Nasza Prowincja ” .
Jest to region rolniczo-przemysłowy , w pasie przybrzeżnym – wypoczynkowy i turystyczny o światowej sławie ( Lazurowe Wybrzeże ) .
Prowansja wchodzi w skład regionu administracyjnego Prowansja-Alpy-Lazurowe Wybrzeże .
Ważniejsze miasta : Marsylia , Tulon , Nicea , Aix , Cannes , Antibes .
Mieszkańcy Prowansji posługują się dialektem prowansalskim języka oksytańskiego ( `` langue d'oc '' ) .
Geografia .
W Prowansji znajduje się południowa część Alp francuskich , z najwyższym jej szczytem – Pelat ( 3051 m n.p.m . ) .
Panuje klimat śródziemnomorski .
Klimat .
Dzięki bliskości Morza Śródziemnego , klimat Prowansji jest łagodny , z gorącym i suchym latem oraz krótką i łagodną zimą .
W zimie często pojawia się okresowy napływ chłodnego powietrza z północy ( wiatr mistral ) .
