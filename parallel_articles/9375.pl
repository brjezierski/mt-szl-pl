Gwary prudnickie
Gwary prudnickie ( alt .
gwary głogóweckie ) – jedna z gwar środkowośląskich języka polskiego .
Cechami charakterystycznymi gwar prudnickich są :
Zasięg występowania .
Gwary prudnickie występują na wschód od Prudnika i na zachód od Odry .
Ich zasięg pokrywa się częściowo z powiatem prudnickim w jego historycznych granicach .
Jest to obszar dzisiejszych powiatów prudnickiego ( jego wschód i północ ) , krapkowickiego ( zachód ) , nyskiego ( wschód ) , kędzierzyńsko-kozielskiego ( zachód ) i głubczyckiego ( północ ) .
Występują wokół Głogówka i Białej , sięgają Krapkowic i Kędzierzyna-Koźla .
Wśród ludności posługującej się gwarami prudnickimi wyróżnia się kilka podgrup gwarowych .
Podział wynika z położenia miejscowości w terenie oraz rodzaju prac , jakie wykonywali mieszkańcy poszczególnych wsi .
Mieszkańców wsi Gostomia , Solec , Rostkowice , Wilków , Mionów , Wierzch , Olbrachcice , Nowy Browiniec i Browiniec Polski , w pobliżu których nie ma lasów , zalicza się do grupy `` Golołków '' ( jest `` goło '' ) .
Ludność zamieszkująca wsie bliżej kompleksów leśnych – Moszną , Zielinę , Nową Wieś Prudnicką , Kujawy , Ścigów , Racławiczki , Dziedzice , Smolarnię i Łącznik , to `` Podlesiołki '' .
Na wschód od nich , w miejscowościach But , Czartowice , Golczowice , Mucków , Sysłów , Wawrzyńcowice , Zawada , mieszkają `` Klocołrzy '' , którzy chodzili do lasów po drzewo .
`` Mietlołrzy '' , którzy trudnili się wyrobem mioteł , to mieszkańcy Steblowa , Dobrej i Strzeleczek .
We wsiach Mochów , Błażejowice Dolne i Dzierżysławice mieszkają `` Kapuściołrzy '' , którzy uprawiali warzywa .
Mieszkańców wsi Rzepcze , Kórnica , Komorniki , Pisarzowice , Pietna , Ściborowice , Nowy Dwór Prudnicki nazywa się `` Kamiyniołrzami '' , ponieważ wsie te znajdują się na kamienistym gruncie .
Ludzi mieszkających w Głogowcu to `` Karpaciołrzy '' , ponieważ mieszkają na wzgórzu .
Biedrzychowice i Naczęsławice to `` Hery '' , natomiast Żużela , Brożec , Stradunia i Mechnica to `` Buchciołrzy '' .
Badania .
Mowa prudnicka była pierwszą naukowo zbadaną polską gwarą .
Badania nad nią rozpoczął Lucjan Malinowski w 1869 .
Prudnickie narzecze badali również : Kazimierz Nitsch , Karol Dejna , Stanisław Bąk , Stanisław Rospond , Stanisław Urbańczyk i Feliks Pluta .
Kultura .
Anna Myszyńska pisała swoje książki i opowiadania w gwarze prudnickiej .
Używał jej również Rafał Urban , który traktował ją twórczo i przekształcał literacko .
