Górka Prudnicka
Górka Prudnicka ( dodatkowa nazwa w j. niem .
Ernestinenberg ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Biała .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Kotliny Raciborskiej , będącej częścią Niziny Śląskiej .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 211 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 13 km od granicy z Czechami , w zachodniej części Kotliny Raciborskiej .
Należy do Euroregionu Pradziad .
Środowisko naturalne .
W Górce Prudnickiej panuje klimat umiarkowany ciepły .
Średnia temperatura roczna wynosi +8,3 °C .
Duże zróżnicowanie dotyczy termicznych pór roku .
