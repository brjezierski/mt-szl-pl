Adam Małysz
Adam Henryk Małysz ( ur .
3 grudnia 1977 w Wiśle ) – polski skoczek narciarski i kierowca rajdowy , uprawiający także kombinację norweską .
W latach 2016–2022 dyrektor koordynator ds .
skoków narciarskich i kombinacji norweskiej w Polskim Związku Narciarskim .
Od 2022 prezes Polskiego Związku Narciarskiego .
Czterokrotny olimpijczyk .
Zdobywca czterech medali olimpijskich ( srebrnego i brązowego w 2002 i dwóch srebrnych w 2010 ) i sześciu medali mistrzostw świata ( złotego i srebrnego w 2001 , dwóch złotych w 2003 , złotego w 2007 i brązowego w 2011 ) .
Czterokrotny zdobywca Pucharu Świata ( w sezonach 2000/2001 , 2001/2002 , 2002/2003 i 2006/2007 ) , trzykrotny zwycięzca Letniego Grand Prix ( 2001 , 2004 i 2006 ) oraz dwukrotny zdobywca Pucharu KOP ( w sezonach 2002/2003 i 2006/2007 ) .
Triumfator 49 .
Turnieju Czterech Skoczni ( 2000/2001 ) , trzech edycji Turnieju Nordyckiego ( 2001 , 2003 i 2007 ) oraz Turnieju Czterech Narodów 2010 .
Zdobywca 39 indywidualnych złotych medali mistrzostw Polski ( 21 zimą i 18 latem ) .
W trakcie kariery wyrównał rekord świata w długości skoku narciarskiego ( 225 m w 2003 ) i osiem razy poprawiał rekord Polski ( po raz ostatni w 2011 , gdy uzyskał 230,5 m ) .
W latach 1995–2011 wystąpił w 349 konkursach Pucharu Świata .
Odniósł 39 zwycięstw , 92 razy stał na podium , 198 razy zajmował miejsce w pierwszej dziesiątce zawodów , a 307 razy zdobywał punkty PŚ .
Łącznie zgromadził 13070 punktów PŚ .
Jako zawodnika cechowało go mocne odbicie z progu i bardzo niska pozycja w locie .
W trakcie kariery skakał na nartach marki Elan ( 1994–1996 oraz 1999–2004 ) , Rossignol ( 1996–1998 ) i Fischer ( 2004–2011 ) .
Po sezonie 2010/2011 zakończył karierę skoczka narciarskiego i rozpoczął starty w rajdach samochodowych .
W 2012 zdobył tytuł międzynarodowego rajdowego mistrza Polski i Czech , a w 2013 odniósł pierwsze w karierze zwycięstwo w rajdach terenowych .
W latach 2012–2016 pięciokrotnie uczestniczył w Rajdzie Dakar .
Za wybitne osiągnięcia sportowe został trzykrotnie odznaczony Orderem Odrodzenia Polski przez Prezydenta RP .
Czterokrotnie wybierany najlepszym sportowcem Polski w plebiscycie „ Przeglądu Sportowego ” ( w 2001 , 2002 , 2003 i 2007 ) .
W 2019 wybrany najlepszym polskim narciarzem w plebiscycie „ Gwiazdy 100-lecia PZN ” .
