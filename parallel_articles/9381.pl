Język pruski
Język pruski , – wymarły język bałtyckich Prusów .
Klasyfikacja .
Język pruski należy do podgrupy języków bałtyckich rodziny języków indoeuropejskich .
Wraz z językiem jaćwińskim zaliczany jest do zachodniobałtyckich .
Charakteryzował się najbardziej archaicznym słownictwem wśród języków bałtyckich .
