Historia .
Język pruski był ojczystym językiem Prusów – ludu bałtyckiego zamieszkującego tereny między dolną Wisłą a dolnym Niemnem .
Każde z plemion mówiło osobnym dialektem , zrozumiałym jednak dla innych .
W 1226 r. książę Konrad I mazowiecki osadził na ziemi chełmińskiej Krzyżaków , którzy wkrótce zagarnęli większość ziem Prusów .
Od tej pory datuje się schyłek staropruskiej kultury i języka .
Resztki ocalałej ludności zostały przeniesione na zachodnie tereny Prus , co spowodowało ujednolicenie języka , choć na biskupiej Warmii utrzymała się odrębność od innych terenów zakonu .
Część wyższych warstw poddała się germanizacji , zaś wolni chłopi zostali zmienieni w poddanych feudalnych .
