Pruszynek
Pruszynek – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 9 km na północny wschód od Siedlec .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
Wierni Kościoła rzymskokatolickiego należą do parafii św .
Mikołaja w Pruszynie .
W Pruszynku znajduje się Ochotnicza Straż Pożarna i świetlica .
Przez wieś przebiega droga powiatowa Pruszyn – Pruszyn-Pieńki .
