Prężyna
Prężyna ( dodatkowa nazwa w j. niem .
Groß Pramsen , ) – wieś w Polsce , położona w województwie opolskim , w powiecie prudnickim , w gminie Biała .
Historycznie leży na Górnym Śląsku , na ziemi prudnickiej .
Położona jest na terenie Wysoczyzny Bialskiej , będącej częścią Niziny Śląskiej .
Przepływa przez nią rzeka Biała .
W latach 1975–1998 miejscowość należała administracyjnie do ówczesnego województwa opolskiego .
Według danych na 2011 wieś była zamieszkana przez 315 osób .
Geografia .
Położenie .
Wieś jest położona w południowo-zachodniej Polsce , w województwie opolskim , około 6,5 km od granicy z Czechami , na Wysoczyźnie Bialskiej , tuż przy granicy gminy Biała z gminą Lubrza .
Należy do Euroregionu Pradziad .
Przez granice administracyjne wsi przepływa rzeka Biała .
Leży na terenie Nadleśnictwa Prudnik ( obręb Prudnik ) .
Środowisko naturalne .
W Prężynie panuje klimat umiarkowany ciepły .
Średnia temperatura roczna wynosi +8,1 °C .
Duże zróżnicowanie dotyczy termicznych pór roku .
