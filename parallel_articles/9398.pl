Przemyśl
Przemyśl ( , , ) – miasto na prawach powiatu w południowo-wschodniej Polsce , w województwie podkarpackim , położone nad Sanem przy ujściu Wiaru .
Siedziba powiatu przemyskiego oraz gminy wiejskiej Przemyśl .
Według danych GUS z 31 grudnia 2020 roku , Przemyśl liczył 59 779 mieszkańców .
Przemyśl był miastem królewskim I Rzeczypospolitej i stolicą ziemi przemyskiej wchodzącej w skład województwa ruskiego .
Był miastem stołecznym starostwa przemyskiego w drugiej połowie XVI wieku .
W czasach Rzeczypospolitej Obojga Narodów w Przemyślu mieściła się kasa szafarzy podatków dla Rusi .
Po odzyskaniu niepodległości przez Polskę od 23 grudnia 1920 do 18 sierpnia 1945 znajdował się na terenie województwa lwowskiego .
Od 1945 do 1974 wchodził w skład województwa rzeszowskiego .
