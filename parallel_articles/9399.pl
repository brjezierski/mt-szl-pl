Przerośl ( jezioro )
Przerośl – jezioro rynnowe w Polsce , w gminie Dubeninki , znajdujące się na granicy otuliny Parku Krajobrazowego Puszczy Rominckiej .
Po północnej stronie jeziora , przy drodze wojewódzkiej nr 651 leży niewielka wieś Kiepojcie .
Powierzchnia jeziora wynosi ok. 65 ha , wymiary : długość w linii prostej – 2200 m , szerokość – 520 m , długość linii brzegowej – ok. 5400 m , maksymalna głębokość – 28,2 m. Dno jeziora jest piaszczysto-kamieniste .
Na północnym krańcu zbiornika znajduje się wypływ rzeki Bludzi , na południu połączone jest z Jeziorem Kościelnym .
