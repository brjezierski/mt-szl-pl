Przesławice ( powiat proszowicki )
Przesławice – wieś w Polsce , położona w województwie małopolskim , w powiecie proszowickim , w gminie Koniusza .
Podczas II wojny światowej miała tutaj miejsce 6 września 1939 kilkugodzinna bitwa stoczona przez 201. pułk piechoty Armii „ Kraków ” pod dowództwem ppłk .
Władysława Adamczyka z oddziałami niemieckimi .
Polscy żołnierze uniemożliwili Wehrmachtowi ( mimo użycia przez Niemców czołgów ) sforsowanie rzeki Szreniawy i osłonili wycofujące się oddziały macierzystej armii .
Obie strony straciły po jednym żołnierzu .
Polskim poległym był Eryk Moryc z Katowic , który został pochowany przy krzyżu przy drodze między Proszowicami a Słomnikami .
W latach 1975–1998 miejscowość należała administracyjnie do województwa krakowskiego .
