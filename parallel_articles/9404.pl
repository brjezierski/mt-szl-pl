Przeworsk
Przeworsk ( , , ) – miasto w Polsce w województwie podkarpackim , siedziba powiatu przeworskiego , na Podgórzu Rzeszowskim , nad Mleczką , przy trasie międzynarodowej E40 , dawna siedziba magnaterii polskiej .
Prywatne miasto szlacheckie lokowane w 1394 roku położone było w XVI wieku w województwie ruskim .
W latach 1975–1998 w województwie przemyskim .
Jest członkiem Związku Miast Polskich .
Według danych z 1 stycznia 2018 Przeworsk liczył 15 452 mieszkańców .
