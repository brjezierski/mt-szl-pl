Alicja w Krainie Czarów ( bajka muzyczna )
Alicja w krainie czarów – polska baśń muzyczna z 1977 na podstawie książki `` Alicja w Krainie Czarów '' .
Nagrana i wydana przez Polskie Nagrania „ Muza ” na płycie SX-1321 i kasecie nr CK-193 .
W ostatnich latach pojawiły się liczne wznowienia na płytach CD wydawane przez firmy prywatne .
