Przyłęków
Przyłęków – wieś w Polsce położona w województwie śląskim , w powiecie żywieckim , w gminie Świnna .
W latach 1975–1998 miejscowość wchodziła w skład województwa bielskiego .
W wiosce znajduje się parafia NMP Wspomożenia Wiernych należącą do diecezji bielsko-żywieckiej .
Opiekę nad nią sprawują księża salezjanie .
W Przyłękowie znajduje się również szkoła podstawowa imienia Janiny Bühl .
Sanktuarium Najświętszej Marii Panny Wspomożenia Wiernych .
Objawienia .
Miejscowość słynie z objawień , które miały miejsce 2 lipca 1886 roku , w uroczystość Nawiedzenia Najświętszej Maryi Panny .
Widzącym był Wojciech Stefko , tutejszy pasterz .
Miał on jedyną córkę Marysię , która była niewidoma .
Modlił się o jej zdrowie i zasnął .
