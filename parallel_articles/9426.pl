Przodkowo
Przodkowo ( dodatkowa nazwa w j. kaszub .
Przedkòwò , niem .
`` Seefeld '' ) – wieś kaszubska w Polsce , położona w województwie pomorskim , w powiecie kartuskim , w gminie Przodkowo , przy drodze wojewódzkiej nr 224 .
W Przodkowie działa jednostka Ochotniczej Straży Pożarnej , przy siedzibie której znajduje się Muzeum Pożarnictwa ( wśród eksponatów znajdują się stare wozy strażackie i dawny sprzęt gaśniczy ) .
Obszar wsi jest podzielony na dwa sołectwa : Przodkowo i Przodkowo Działki .
Wieś królewska w powiecie gdańskim województwa pomorskiego w II połowie XVI wieku .
W latach 1954-1972 wieś należała i była siedzibą władz gromady Przodkowo .
W latach 1975–1998 miejscowość administracyjnie należała do województwa gdańskiego .
Miejscowość jest siedzibą gminy Przodkowo .
