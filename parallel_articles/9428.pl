Przemków
Przemków ( , ) – miasto w województwie dolnośląskim , w powiecie polkowickim , siedziba gminy miejsko-wiejskiej Przemków .
Położone jest na północnym krańcu Borów Dolnośląskich , przy drodze krajowej nr 12 .
Według danych GUS z 31 grudnia 2019 r. miasto liczyło 6091 mieszkańców .
W 1954 r. utworzono gromadę Przemków , ale zanim zaczęła funkcjonować , przekształcono ją w osiedle , któremu 1 stycznia 1959 nadano status miasta .
W latach 1954–1972 Przemków był siedzibą władz gromady Przemków , do której nie należał .
1 stycznia 1973 , uchwałą Wojewódzkiej Rady Narodowej w Zielonej Górze , utworzono na nowo gminę Przemków .
