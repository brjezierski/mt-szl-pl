Przysłop
Etymologia słowa `` Przysłop '' nie jest jednoznacznie rozstrzygnięta .
Występuje na terenie całych Karpat ( i nie tylko ) w różnych formach : `` Przysłop '' , `` Prislop '' , `` Przysłup '' , `` Prislopul '' .
Najprawdopodobniej pochodzi od starosłowiańskiego słowa `` prislop '' ( „ przejście przez górski masyw , przełęcz ” ) .
W języku rumuńskim słowo `` prislopul '' oznacza „ wąwóz górski , górską przełęcz ” ( np .
przełęcz Prislopul Negru – 1848 m n.p.m. , Prislop w górach Piatra – 1317 m n.p.m .
i inne ) .
Nasuwa się więc wniosek , że nazwa ta została rozpropagowana przez Wołochów .
Na Słowacji nazwę `` Prislop '' noszą przełęcze ( Prislop na wschód od Kubińskiej Holi – 840 m n.p.m. , Sedlo Prislop w Małej Fatrze – 909 m n.p.m. , Prislop i Velky Prislop w Niżnych Tatrach i inne ) .
W Polsce `` Przysłop '' i `` Przysłup '' to często występujące nazwy przełęczy w Karpatach .
Nazwy szczytów utworzone zostały przez kartografów .
