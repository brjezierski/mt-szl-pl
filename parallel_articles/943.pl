Addis Abeba
Addis Abeba ( , „ Nowy Kwiat ” ) – największe miasto i stolica Etiopii .
Położone jest w centrum kraju , na wysokości ok. 2400 m n.p.m .
na Wyżynie Abisyńskiej , u podnóża wygasłego wulkanu Entoto .
W spisie z 2007 roku populację miasta oszacowano na 2 739 551 mieszkańców , a według projekcji z 2021 roku populacja wzrosła do 3,8 mln mieszkańców .
Ze względu na dużą liczbę działających tutaj organizacji międzynarodowych , oraz bogatą historię i kulturę miasta , Addis Abeba jest czasami określana mianem stolicy Afryki .
Oprócz siedziby Unii Afrykańskiej , wielu biur ONZ – w tym siedziby Komisji Gospodarczej Narodów Zjednoczonych ds .
Afryki – w mieście znajduje się ponad 100 ambasad .
Jest najwyżej położoną stolicą Afryki , oraz piątą najwyżej położoną stolicą na świecie .
Struktura etniczna .
Według danych z 2007 roku , mieszkańcy miasta stanowią mieszankę ok. 80 narodowości ( w tym : Amhara : 47 % , Oromowie : 19,5 % , Gurage : 16,3 % ) ; spośród nich wybijają się społeczności chrześcijańska ( etiopskie prawosławie : 74,7 % , protestantyzm : 7,8 % ) i muzułmańska ( 16,2 % ) , a języki dominujące spośród ponad 80 języków występujących w całej Etiopii to amharski , oromo , oraz gurage .
