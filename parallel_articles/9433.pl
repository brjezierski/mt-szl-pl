Jednak w inżynierii , bądź w teorii grafów , promień jest odcinkiem łączącym środek jakiejkolwiek figury lub bryły ( np .
walca , wielokąta , grafu ) z dowolnym punktem , który może być położony również poza nią .
W takim właśnie przypadku promień nie musi być o połowę krótszy od średnicy , a może być nawet od niej dłuższy .
