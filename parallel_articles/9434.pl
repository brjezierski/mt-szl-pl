Prędocinek ( Radom )
Prędocinek – osiedle w Radomiu .
Dawniej wieś , znana też pod nazwą Prędocin .
Zabudowa osiedla to w większości bloki czteropiętrowe .
W latach międzywojennych przy istniejącej do dziś ulicy Wyścigowej znajdowały się tory wyścigów konnych - zlikwidowane po 1945 roku .
Dojazd liniami autobusowymi nr 3 , 4 , 9 , 21
