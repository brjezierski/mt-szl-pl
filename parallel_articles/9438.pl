Parasola conopilus
Parasola conopilus ( Kauffman ) A.H. Sm .
– gatunek grzybów z rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psathyrella , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1926 r. Gary Kauffmann nadając mu nazwę `` Hypholoma canoceps '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1941 r. Alexander Hanchett Smith , przenosząc go do rodzaju `` Psathyrella '' .
Synonimy naukowe :
Franciszek Błoński w 1889 r. opisywał ten gatunek jako kołpaczek stożkowaty , Władysław Wojewoda w 2003 r. nadał mu nazwę kruchaweczka twardotrzonowa .
Po przeniesieniu tego gatunku do utworzonego w 2001 r. nowego rodzaju `` Parasola '' obydwie polskie nazwy są już niespójne z nazwą naukową .
Morfologia .
Wysokość 2-5 cm .
