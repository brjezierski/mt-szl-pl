Kruchaweczka białoosłonowa
Kruchaweczka białoosłonowa ( `` Psathyrella gordonii '' ( Berk .
& amp ; Broome ) A. Pearson & amp ; Dennis ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psathyrella , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisali w 1861 r. Miles Joseph Berkeley i Christopher Edmund Broome nadając mu nazwę `` Agaricus gordonii '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Richard William George Dennis i Arthur Anselm Pearson w 1947 r .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Średnica 2–5 cm , początkowo półkulisty , potem wypukły z tępym lub ostrym garbem .
Jest higrofaniczny , przy brzegu rowkowany z prześwitującymi blaszkami .
