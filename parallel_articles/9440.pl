Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Typhrasa , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1789 r. Jean Baptiste François Bulliard nadając mu nazwę `` Agaricus gossypinus '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu L. Örstadius i E. Larss .
