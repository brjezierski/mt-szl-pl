Kruchaweczka najdelikatniejsza
Kruchaweczka najdelikatniejsza , kruchaweczka nietykalna ( `` Psathyrella noli-tangere '' ( Fr . )
A. Pearson & amp ; Dennis ) – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psathyrella , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1838 r. Elias Fries nadając mu nazwę `` Agaricus noli-tangere '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Arthur Anselm Pearson i Richard William George Dennis w 1947 r .
Synonimy :
Stanisław Domański w 1955 r. nadał temu gatunkowi polską nazwę kruchaweczka nietykalna .
