Kruchaweczka popielatoblaszkowa
Kruchaweczka popielatoblaszkowa ( `` Psathyrella tephrophylla '' ( Romagn . )
Bon – gatunek grzybów należący do rodziny kruchaweczkowatych ( `` Psathyrellaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psathyrella , Psathyrellaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1852 r. Henri Charles Louis Romagnesi nadając mu nazwę `` Drosophila tephrophylla '' .
Obecną , uznaną przez Index Fungorum nazwę nadał Marcel Bon w 1983 r .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Występowanie .
Znane jest występowanie `` Psathyrella tephrophylla '' tylko w niektórych krajach Europy .
Władysław Wojewoda w swoim zestawieniu grzybów wielkoowocnikowych Polski w 2003 r. podaje tylko 2 stanowiska z uwagą , że jego rozprzestrzenienie w Polsce i stopień zagrożenia nie są znane .
Grzyb saprotroficzny .
Występuje w liściastych lasach i zaroślach na ziemi wśród traw .
