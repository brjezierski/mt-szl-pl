Pseudocercospora opuli
Pseudocercospora opuli U. Braun & amp ; Crous – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożytujący na liściach kaliny koralowej ( `` Viburnum opulus '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudocercospora , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1907 r. Franz von Höhnel .
W 2003 r. U. Braun i Crous w oparciu o wyniki analiz sekwencji molekularnych i ponowną ocenę morfologiczną przenieśli go do rodzaju `` Pseudocercospora '' .
Synonimy :
Charakterystyka .
Na obydwu stronach porażonych liści powstają okrągłe lub kanciaste plamy o średnicy 1–8 mm .
Często sąsiednie plamki zlewają się z sobą .
Miejscami są nieco strefowane , początkowo matowozielone , potem ochrowe do brązowych , w środkowej części z czasem stają się szarawe do szaro-białych z wąskim , ciemnym obrzeżem .
