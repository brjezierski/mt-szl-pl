Pseudocercospora viburnigena
Pseudocercospora viburnigena U. Braun & amp ; Crous – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , pasożytujący na liściach niektórych gatunków kalin ( `` Viburnum '' ) .
Na obydwu stronach liści porażonych roślin powoduje powstawanie rozproszonych plam o średnicy 4–15 mm i barwie od jasnobrązowej do ciemnobrązowej , otoczonych lekko uniesioną ciemnobrązową obwódką .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudocercospora , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1878 r. P.A .
Saccardo nadając mu nazwę `` Cercospora tinea '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu U. Braun i Crous w 2002 r .
