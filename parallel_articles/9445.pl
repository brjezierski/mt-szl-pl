Lejkownik pępówkowaty
Lejkownik pępówkowaty ( `` Pseudoclitocybe obbata '' ( Fr . )
Singer ) – gatunek grzybów z rodziny `` Pseudoclitocybaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudoclitocybe , Pseudoclitocybaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1838 r. Elias Fries nadając mu nazwę `` Agaricus obbata '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1961 r. Rolf Singer .
Synonimy : :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r .
Występowanie i siedlisko .
Podano występowanie lejkownika pępówkowatego w Ameryce Północnej , niektórych krajach Europy i w Japonii .
W. Wojewoda w zestawieniu wielkoowocnikowych grzybów podstawkowych Polski przytacza tylko jedno stanowisko i to przedwojenne ( Elbląg 1913 ) .
Później nie był już w Polsce notowany .
W. Wojewoda sugeruje , że być może jest to gatunek w Polsce wymarły .
W internetowym atlasie grzybów w 2015 i 2016 r. podano jednak dwa zweryfikowane stanowiska tego gatunku .
Znajduje się na liście gatunków zagrożonych wartych objęcia ochroną .
Grzyb saprotroficzny .
Na stanowiskach w Polsce występował w lasach iglastych w Elblągu , w trawie na obrzeżu Puszczy Knyszyńskiej oraz na Śląsku przy polnej drodze , obok piaszczystych nieużytków zarastających młodą sosną z domieszkami innych drzew .
