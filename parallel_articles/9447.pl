Pseudopeziza medicaginis
Pseudopeziza medicaginis ( Lib . )
Sacc . )
– gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Pasożyt lucerny , wraz z gatunkiem `` Pseudopeziza trifolii '' wywołujący chorobę o nazwie kustrzebka koniczyny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudopeziza , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozowała w 1832 r. Marie-Anne Libert , nadając mu nazwę `` Phacidium medicaginis '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu w 1887 r. Pier Andrea Saccardo .
