Pseudopeziza trifolii
Pseudopeziza trifolii ( Biv . )
Fuckel ) – gatunek grzybów z rodziny `` Drepanopezizaceae '' .
Pasożyt roślin , wraz z gatunkiem `` Pseudopeziza medicaginis '' wywołujący chorobę o nazwie kustrzebka koniczyny .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pseudopeziza , Drepanopezizaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1816 r. Antonius de Bivona-Bernardi , nadając mu nazwę `` Ascobolus trifolii '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu w 1870 r. Leopold Fuckel .
