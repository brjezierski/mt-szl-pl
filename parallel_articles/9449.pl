Pseudonim
Pseudonim ( z `` pseudonymos '' , dosł .
„ pod fałszywym imieniem ” ) – indywidualna nazwa danej osoby , inna niż jej oficjalne imię i nazwisko .
Najczęściej pseudonim stosowany jest w celu ukrycia oficjalnych personaliów , czasami też jego nadanie ma ułatwić zapamiętanie jakiejś osoby lub ma podkreślać czyjeś cechy osobowe czy fizyczne .
Pseudonim można wybrać sobie samemu , lecz przeważnie bywa on nadawany osobie przez grupę , w której jest później używany .
Pierwsze w Polsce udokumentowane użycie pseudonimu miało miejsce pod koniec XV wieku , kiedy to Filippo Buonaccorsi zaczął podpisywać się `` Callimachus Etruscus '' , `` Kallimachus Philippus '' , `` Callimacho Romano '' .
Pseudonimów używali bojownicy OB PPS podczas Rewolucji 1905 roku , a także członkowie polskiego podziemia niepodległościowego i antykomunistycznego w XIX i XX w. , m.in .
przedstawiciele Armii Krajowej , Batalionów Chłopskich oraz Armii Ludowej .
