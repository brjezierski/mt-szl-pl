Psilocybe aztecorum
Psilocybe aztecorum R. Heim – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Podobnie jak inne gatunki z rodzaju `` Psilocybe '' , jest grzybem halucynogennym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Morfologia .
Barwa kapelusza zmienia się w zależności od stanu nawodnienia , od brązowego do słomkowego lub białawego w czasie suszy .
Podstawa trzonu pokryta jest widocznymi białymi ryzomorfami , co jest cechą wyjątkową wśród grzybów z rodzaju łysiczka .
Występowanie i siedlisko .
Opisano występowanie `` Psilocybe aztecorum '' tylko w wysokich górach środkowego Meksyku , takich jak Sierra Nevada , Nevado de Toluca , La Malinche w Nowym Meksuku , Puebla i Tlaxcala .
Rośnie tutaj na wysokości 2000 – 330 m n , p , m .
Według Guzmán , jest prawdopodobne , że gatunek ten rośnie także w innych obszarach górskich , takich jak of Nuevo Leon , Veracruz , Colima , and Chiapas , mają one bowiem podobne warunki siedliskowe .
