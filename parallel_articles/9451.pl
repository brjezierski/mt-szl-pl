Psilocybe azurescens
Psilocybe azurescens – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Morfologia .
O średnicy 30-100 mm średnicy , stożkowy do wypukłego , higrofaniczny , w kolorze brązowym , ochrowym lub karmelowym , jaśniejszy na obwodzie .
Powierzchnia gładka , lepka kiedy jest wilgotna .
90-200 mm długości na 3-6 mm średnicy , jest biały lub brudnobrązowy .
Czarny , brązowy lub ciemnofioletowy .
Występowanie i siedlisko .
Uprawiany jest na całym świecie , ale naturalnie występuje tylko w Ameryce Północnej , wzdłuż wybrzeża Oceanu Spokojnego .
Saprotrof .
Rośnie kępkach w lasach oraz na nadmorskich wydmach , na podłożu bogatym w próchniejące resztki drzewa .
Znaczenie .
Jest najbardziej zasobnym w alkaloidy grzybem psylocybinowym .
Może zawierać do 1,8 % psylocybiny , 0,5 % psylocyny i 0,4 % baeocystyny w suchej masie , średnio jednak wartości te utrzymują się na poziomie 1,1 % psylocybiny i 0,15 % psylocyny .
Zawiera w dużym stężeniu psychoaktywne alkaloidy : głównie psylocybinę , psylocynę i baeocystynę .
Po zażyciu jednego grama wysuszonego `` Psilocybe azurescens '' następują trwające 6 godzin psychodeliczne stany wizyjne .
Działanie grzybów psylocybinowych jest bardzo indywidualne i nieprzewidywalne , ale zwykle obejmuje : poprawienie humoru i podniesienie nastroju , zaburzenia percepcji , euforię , efekty wizualne oraz ogólną zmianę postrzegania zmysłowego .
