Łysiczka czeska
Łysiczka czeska ( `` Psilocybe bohemica '' Šebek ex Šebek ) – gatunek grzybów należący do rodziny `` Hymenogastraceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psilocybe , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy nazwy naukowej :
Nazwę polską podają niektóre atlasy grzybów
Morfologia .
20-50 mm średnicy , higiograficzny , suchy jasnoochrowy , wilgotny rdzawobrązowy
Blaszki na początku jasnobrązowe , z wiekiem ciemnobrązowe .
Biały , połyskujący , siniejący , 50-80 mm .
Występowanie .
Występuje głównie w Europie .
Jest rzadki , jego owocniki pojawiają się pojedynczo lub w grupach , na zbutwiałym drewnie , na kompoście , w ogrodach , parkach , na poboczach dróg .
Znaczenie .
Łysiczka czeska zawiera dość znaczne stężenie psychoaktywnych alkaloidów jak psylocybina i psylocyna .
Po zażyciu około 2 gramów wysuszonej łysiczki czeskiej występują trwające 6 godzin psychodeliczne stany wizyjne .
Działanie grzybów psylocybinowych bywa bardzo indywidualne i nieprzewidywalne , ale zwykle obejmuje : poprawienie humoru i podniesienie nastroju , zaburzenia percepcji , efekty wizualne oraz ogólną zmianę postrzegania zmysłowego .
Gatunki podobne .
`` Psilocybe cyanescens '' występujący w Ameryce Północnej , ale stopniowo rozprzestrzeniający się także w Europie .
Podobne są także `` Psilocybe serbica '' oraz `` Psilocybe mairei '' .
