Psilocybe mexicana
Psilocybe mexicana R. Heim – gatunek grzybów z rodziny pierścieniakowatych ( `` Strophariaceae '' ) .
Morfologia .
Niewielki , do 5 cm wysoki grzyb kapeluszowy o brązowawym kapeluszu , osiągający 12-20 mm średnicy , z blaszkowatym hymenoforem .
Tworzy sklerocja .
Występowanie .
Grzyb ten jest koprofagiem – żyje na nawozie lub w miejscach silnie zanieczyszczonych odchodami zwierzęcymi .
Występuje od Meksyku do Gwatemali .
