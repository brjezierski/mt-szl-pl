Łysiczka błotna
Łysiczka błotna `` ( Psilocybe strictipes ) '' - gatunek grzybów należący do rodziny podziemniczkowatych ( `` Hymenogastraceae '' ) .
Zawiera psychoaktywną psilocybinę , jest blisko spokrewniony z łysiczką lancetowatą i `` Psilocybe pelliculosa '' .
Łysiczka błotna jest powszechnie mylona z lancetowatą i można ją odróżnić po kapeluszu bez brodawki i o wypukłym kształcie .
Nazwa gatunkowa „ strictipes ” pochodzi od łacińskich słów `` stricti '' ( wąski ) i `` pes '' ( stopa ) .
Nazwę polską zaproponował Władysław Wojewoda w 2003 r ..
Morfologia .
Średnica 5-30mm , higrofaniczny , wilgotny , koloru od orzechowego do ciemnobrązowego .
Powierzchnia gładka , wilgotna , śliska , nieco lepka .
Kształt wypukły i zwykle bez ostrej brodawki typowej dla `` P. semilanceata '' .
Szerokie , do trzonu wąsko przyrośnięte .
U młodych owocników kremowe , u starszych ciemniejsze – ciemnofioletowo-brązowe .
Cienki , o wysokości 4-10 cm i średnicy 2.5 mm .
Powierzchnia jasnoochrowa .
Ma zapach i smak mąki , przebarwia się na niebiesko .
Wysyp zarodników ciemny fioletowo-brązowy .
Zarodniki podłużne , owalne , o rozmiarach 11 × 6 μm .
Występowanie i siedlisko .
Występuje w chłodnych umiarkowanych i subarktycznych regionach półkuli północnej i jest najbardziej powszechna w Europie oraz północno-zachodnim wybrzeżu Ameryki Północnej .
`` Psilocybe strictipes '' owocuje późnym latem i jesienią w Polsce , Chile , Anglii , Francji , Niemczech , Holandii , Szkocji , Słowacji , Syberii , Szwecji i północno-zachodnim Pacyfiku .
`` Psilocybe strictipes '' występuje na trawnikach i trawiastych polach , ale nigdy nie rośnie bezpośrednio na łajnie .
