Łysiczka ciemnobrązowa
Łysiczka ciemnobrązowa ( `` Psilocybe subviscida '' ( Peck ) Kauffman ) – gatunek grzybów należący do rodziny podziemniczkowatych ( `` Hymenogastraceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Psilocybe , Hymenogastraceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy :
Nazwę polską zaproponował Władysław Wojewoda w 2003 r ..
Morfologia .
Średnica 0,5–2 cm , początkowo dzwonkowaty , potem wypukły z tępym garbkiem , na koniec niemal płaski .
Jest higrofaniczny ; w stanie wilgotnym ma prążkowane brzegi , barwę od żółtawej do kasztanowo brązowej i jest lepki lub nieco lepki , w stanie suchym jest jasno szarożółty , tylko garbek pozostaje czerwonawobrązowy .
