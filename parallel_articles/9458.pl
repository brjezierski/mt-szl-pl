Pszczyna ( stacja kolejowa )
Pszczyna – stacja kolejowa w Pszczynie , w województwie śląskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca aglomeracyjnego .
Znajduje się na wysokości 232 m n.p.m .
