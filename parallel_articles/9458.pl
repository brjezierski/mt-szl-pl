Historia .
Stacja powstała 15 listopada 1868 na odcinku Tychy – Czechowic-Dziedzice niemieckiej Kolei Prawego Brzegu Odry ( `` Rechte-Oder-Ufer-Eisenbahn '' ) .
21 listopada 1938 stacja stała się węzłową , gdy otwarto do użytku linię kolejową z Rybnika do Pszczyny .
W budynku dworca powstałego w 1890 roku znajduje się poczekalnia z kasą biletową .
W latach 2012–2014 przeprowadzono kosztem 2,5 mln zł ( netto ) gruntowny remont dworca , inwestycja została sfinansowana z budżetu państwa i środków własnych PKP .
Otwarcie kompleksowo wyremontowanego dworca miało miejsce 16 września 2014 .
Od 12 listopada 2014 do 4 listopada 2019 w budynku dworca działał Powiatowy Punkt Informacji Turystycznej , który następnie przeniesiono do nowo powstałego budynku centrum przesiadkowego .
Ruch pociągów .
Stacja obsługuje ruch lokalny Katowice – Bielsko-Biała/Zwardoń , Katowice – Cieszyn/Wisła , Bielsko Biała – Racibórz/Wodzisław Śląski , a także ruch dalekobieżny : pociągi spółki PKP Intercity do Warszawy , Wrocławia , Gdyni i Bielska-Białej , w tym pociągi Express InterCity Premium .
