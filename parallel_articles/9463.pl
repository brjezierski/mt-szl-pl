Ptaki ( powiat łomżyński )
Ptaki – wieś w Polsce położona w województwie podlaskim , w powiecie łomżyńskim , w gminie Nowogród .
Wierni kościoła rzymskokatolickiego należą do parafii Narodzenia Najświętszej Maryi Panny w Nowogrodzie .
Historia .
W latach 1921–1939 wieś leżała w województwie białostockim , w powiecie kolneńskim ( od 1932 w powiecie łomżyńskim ) , w gminie Rogienice .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 11 osób w 2 budynkach mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej w m. Nowogród .
Podlegała pod Sąd Grodzki w Stawiskach i Okręgowy w Łomży ; właściwy urząd pocztowy mieścił się w m. Nowogród .
W wyniku napaści ZSRR na Polskę we wrześniu 1939 gmina znalazła się pod okupacją sowiecką .
2 listopada została włączona do Białoruskiej SRR .
4 grudnia 1939 włączona do nowo utworzonego obwodu białostockiego .
Od czerwca 1941 roku pod okupacją niemiecką .
Od 22 lipca 1941 do wyzwolenia w styczniu 1945 włączona w skład Landkreis Lomscha , Bezirk Bialystok III Rzeszy .
W latach 1975–1998 miejscowość należała administracyjnie do województwa łomżyńskiego .
