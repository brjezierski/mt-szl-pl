Puccinia acetosae
Puccinia acetosae ( Schumach . )
Körn .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny powodujący u roślin chorobę zwaną rdzą .
Pasożytuje na różnych gatunkach z rodziny rdestowatych ( `` Polygonaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten zdiagnozował w 1803 r. Christian Friedrich Schumacher nadając mu nazwę `` Uredo acetosae '' .
