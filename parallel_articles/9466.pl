Puccinia adoxae
Puccinia adoxae R. Hedw .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożytujący na piżmaczku wiosennym ( `` Adoxa moschatellina '' ) .
Jest pasożytem obligatoryjnym , u porażonych roślin wywołuje chorobę zwaną rdzą .
Występuje w Europie i Ameryce Północnej ( USA ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Rozwój i morfologia .
Jest pasożytem jednodomowym , tzn .
cały jego cykl rozwojowy odbywa się na jednym żywicielu .
Jest też rdzą niepełnocyklową – wytwarza tylko jeden rodzaj zarodników – teliospory .
Poraża liście piżmaczka wiosennego wraz z ogonkami liściowymi .
Tworzą się na nich ciemnobrązowe plamy .
Pod skórką w obrębie plam znajdują się telia z teliosporami .
Często plamy znajdują się na nabrzmieniach podobnych do galasów .
W okresie dojrzewania zarodników telia pęcznieją , co powoduje pęknięcia skórki , przez które zarodniki wydostają się na zewnątrz .
Gatunki podobne .
Na piżmaczku wiosennym pasożytują jeszcze 2 inne gatunki grzybów z rodzaju `` Puccinia '' : `` Puccinia albescens '' i `` Puccinia argentata '' .
`` P. albescens '' na piżmaczku morfologicznie różni się nieznacznie , ale zasadniczo odróżnia się cyklem rozwojowym – wytwarza wszystkie 5 typowych dla rdzy zarodników .
