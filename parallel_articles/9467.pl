Puccinia aegopodii
Puccinia aegopodii ( Schumach . )
Link – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożytujący na podagryczniku pospolitym ( `` Aegopodium podagraria '' ) .
Jest pasożytem obligatoryjnym , wywołuje chorobę zwaną rdzą .
Występuje w całej Europie i Azji .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1803 r. Ch.F .
