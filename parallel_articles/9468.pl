Puccinia apii
Puccinia apii Desm .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny powodujący u roślin chorobę zwaną rdzą .
Pasożytuje na selerze ( `` Apium '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonim : '' Trichobasis apii '' ( Desm . )
Cooke 1878 .
Charakterystyka .
Jest pasożytem jednodomowym , tzn .
cały jego cykl rozwojowy odbywa się na jednym żywicielu .
Jest rdzą pełnocyklową – wytwarza wszystkie typowe dla rdzy rodzaje zarodników .
Spermogonia , ecja , uredinia i telia powstają głównie na dolnej stronie liści .
Ecja małe , kopułkowate , białe .
Ecjospory pomarańczowe , drobno brodawkowate , bez gładkich stref ( w przeciwieństwie do `` Uromyces lineolatus '' ) .
Uredinia proszkowate , o barwie cynamonowej .
Urediniospory jednokomórkowe .
Telia czarniawo brązowe , teliospory dwukomórkowe , na trzonku .
`` Puccinia apii '' to gatunek monofagiczny , pasożytujący na różnych gatunkach selera ( `` Apium '' ) .
Opisano jego występowanie w Ameryce Południowej i w Europie .
W Polsce podano jeden przypadek jego wystąpienia na selerze zwyczajnym ( `` Apium graveolens '' ) .
