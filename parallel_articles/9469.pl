Puccinia arenariae
Puccinia arenariae ( Schumach . )
J. Schröt .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Pasożyt obligatoryjny roślin z rodzin goździkowate ( `` Caryophyllaceae '' ) , zdrojkowate ( `` Montiaceae '' ) i ugłastowate ( `` Molluginaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1803 r. Ch.F .
Schumacher nadając mu nazwę `` Uredo arenariae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Joseph Schröter w 1880 r .
