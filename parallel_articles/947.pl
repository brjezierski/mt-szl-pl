Adigrat
Adigrat ( ) – miasto i wydzielona woreda w północnej Etiopii , w regionie Tigraj .
Miasto uważane jest za bramę do Erytrei i Morza Czerwonego .
Geografia .
Adigrat otoczone jest pasmem górskim ; położone jest na skrzyżowaniu szlaków handlowych między miastami z północnej części kraju a Erytreą i Morzem Czerwonym .
Przez miasto przepływa rzeka Huga .
Adigrat leży na średniej wysokości 2457 m n.p.m .
Klimat .
Miasto położone jest w klimacie śródziemnomorskim .
Roczne opady wahają się między 400–600 mm , przy czym większość opadów ma miejsce w porze deszczowej ( od czerwca do lipca ) .
Demografia .
Według spisu powszechnego z 2007 roku w Adigrat mieszkało 57 588 mieszkańców , z czego 31 578 stanowiły kobiety .
