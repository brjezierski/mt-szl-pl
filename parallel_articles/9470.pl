Puccinia argentata
Puccinia argentata ( Schultz ) G. Winter – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Jest szeroko rozprzestrzeniony w Ameryce Północnej i Europie , występuje także w Japonii .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1806 r. Carl Friedrich Schultz nadając mu nazwę `` Aecidium argentatum '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu w 1880 r. Heinrich Georg Winter .
