Jest pasożytem dwudomowym , tzn .
że jego pełny cykl życiowy odbywa się na dwóch różnych gatunkach roślin .
Jest też rdzą pełnocyklową , tzn .
że tworzy wszystkie rodzaje zarodników typowych dla rdzowców .
Na porażonych liściach piżmaczka wiosennego ( `` Adoxa moschatellina '' ) tworzą się nabrzmiałe plamy , a w nich powstają ecja i spermogonia .
Ecja mają śnieżnobiałe perydium i powstają w nich żółte ecjospory .
Drugim żywicielem są różne gatunki niecierpka ( `` Impatiens '' ) .
W obrębie żółtych plam na liściach tworzą się w rozproszeniu , lub na obwodzie okręgu żółtawobrązowe uredinia i telia .
Urediniospory z 5–6 porami rostkowymi .
Teliospory dwukomórkowe ze szklistą brodawką .
Na piżmaczku wiosennym mogą pasożytować jeszcze dwa inne gatunki rdzy : `` Puccinia albescens '' i `` Puccinia adoxae '' .
