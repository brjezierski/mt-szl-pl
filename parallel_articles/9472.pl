Rozwój i morfologia .
Jest pasożytem jednodomowym , tzn .
cały jego cykl rozwojowy odbywa się na jednym żywicielu .
Jest też rdzą niepełnocyklową , wytwarza tylko jeden rodzaj zarodników – teliospory .
Teliospory gładkie , dwukomórkowe , w 10 % roztworze amoniaku barwią się na jasnożółtobrązowo .
Mają rozmiar 28–40 ( –44 ) × 14–23 μm .
Ściana o grubości 1–2 μm , ale na wierzchołkach dużo grubsza – 5–7 ( –8 ) μm .
Obydwie komórki posiadają pojedyncze pory rostkowe .
Osadzone są na łatwo odpadającym trzonku o średnicy 2–4 μm .
Na obydwu stronach liści kopytnika `` Puccinia asarina '' powoduje powstawanie chlorotycznych pęcherzy o średnicy 0,5–1 cm .
Tworzą się w nich pod skórką liści brązowawe , podobne do krost telia .
Teliospory wydostają się z nich przez pęknięcia w skórce .
