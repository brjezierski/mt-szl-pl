Puccinia asarina
Puccinia asarina Kunze – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożyt roślin z rodzaju kopytnik ( `` Asarum '' ) .
Jest pasożytem obligatoryjnym , u porażonych roślin wywołuje chorobę zwaną rdzą .
Grzyb ten podany został jako zawleczony do Europy ( obecnie tu rozpowszechniony jako pasożyt kopytnika pospolitego ) , przy czym pierwotnie miał występować na dwóch gatunkach kopytników występujących we wschodniej części Ameryki Północnej .
