Puccinia asparagi
Puccinia asparagi DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na różnych gatunkach szparagów ( `` Asparagus '' ) .
Wywołuje u nich chorobę zwaną rdzą szparaga .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia i rozwój .
Jest pasożytem jednodomowym , tzn .
cały jego cykl rozwojowy odbywa się na jednym żywicielu .
Jest rdzą pełnocyklową , wytwarzającą 5 rodzajów zarodników : sporydia , pykniospory , ecjospory , urediniospory i teliospory .
Na porażonych szparagach powstają kropkowate , brązowe pyknidia , a nieco później pomiędzy nimi w skupiskach kubkowate lub prawie cylindryczne ecja osadzone na trzonkach .
Ecjospory o kształcie od kulistego do owalnego , wymiarach 15–21 × 18–27 μm i niemal hialinowej , drobno i gęsto brodawkowanej ścianie o grubości 1 μm .
Uredinia również na trzonkach , kuliste do elipsoidalnych , cynamonowobrązowe , mączyste .
