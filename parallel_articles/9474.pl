Charakterystyka .
`` Puccinia betonicae '' jest pasożytem jednodomowym , tzn .
cały jej cykl rozwojowy odbywa się na jednym żywicielu .
Jest rdzą niepełnocyklową , wytwarzającą tylko jeden rodzaj zarodników – teliospory powstające w rdzawobrązowych teliach na liściach .
Telia te tworzą się głównie na górnej stronie liści .
Na porażonych liściach następuje chloroza , liście blakną , mają wydłużony ogonek i nienormalny wygląd .
Teliospory dwukomórkowe , gładkie , na krótkim trzonku .
Występuje w Europie i Rosji .
Jest to monofag występujący tylko na wąskiej grupie gatunków roślin z rodzaju `` Stachys '' .
Stwierdzono jego występowanie na bukwicy zwyczajnej ( `` Stachys officinalis '' ) i `` Stachys pradica '' .
