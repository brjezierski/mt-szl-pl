Puccinia bistortae
Puccinia bistortae ( F. Strauss ) DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na różnych gatunkach rdestów ( `` Persicaria '' ) i roślin z rodziny selerowatych ( `` Apiaceae '' ) .
Występuje w Ameryce Północnej , Europie i Azji .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1811 r. nadając mu nazwę `` Uredo bistortae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Augustin Pyramus de Candolle w 1815 r .
Synonimy :
Morfologia i rozwój .
Wytwarza 4 rodzaje zarodników : sporydia , ecjospory , urediniospory i teliospory .
