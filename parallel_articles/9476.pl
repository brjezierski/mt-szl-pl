Puccinia calcitrapae
Puccinia calcitrapae DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny będący pasożytem obligatoryjnym wielu gatunków roślin .
Systematyka i nazewnictwo .
