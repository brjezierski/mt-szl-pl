Puccinia calthicola
Puccinia calthicola J. Schröt .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na knieci ( `` Caltha '' ) .
Wywołuje u niej chorobę zwaną rdzą .
