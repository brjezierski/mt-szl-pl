Charakterystyka .
`` Puccinia calthicola '' jest pasożytem jednodomowym , tzn .
cały jej cykl rozwojowy odbywa się na jednym żywicielu .
Jest rdzą pełnocyklową , wytwarzającą wszystkie typowe dla rdzy rodzaje zarodników .
Spermogonia tworzą się na obydwu stronach blaszki liściowej , ecja i uredinia tylko na dolnej stronie blaszki .
Uredinia mają postać żółtobrązowych grudek .
Powstające w nich urediniospory są jednokomórkowe , kolczaste z 2–3 równikowymi porami rostkowymi .
Ciemnobrązowe telia tworzą się na dolnej stronie blaszki , są nagie i mają postać pylących grudek .
Powstające w nich teliospory są szeroko eliptyczne , dwukomórkowe , drobno brodawkowate , na krótkim trzonku .
Występuje w Europie , Ameryce Północnej i Japonii .
Jest to monofag występujący tylko na roślinach z rodzaju knieć ( `` Caltha '' ) .
W Polsce stwierdzono jego występowanie na knieci błotnej ( `` Caltha palustris '' ) i jej podgatunku – knieci błotnej górskiej ( `` Caltha palustris '' subsp .
`` laeta '' ) .
