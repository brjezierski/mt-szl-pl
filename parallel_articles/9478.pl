Puccinia campanulae
Puccinia campanulae DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju dzwonek ( `` Campanula '' ) i jasieniec ( `` Jasione '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia i rozwój .
Jest rdzą jednodomową , tzn , że cały cykl rozwojowy odbywa się na jednym żywicielu i rdzą niepełnocyklową wytwarzającą tylko jeden rodzaj zarodników – dwukomórkowe teliospory .
Grudkowate telia powstają na dolnej stronie liści .
Nad nimi , na górnej stronie liści tworzą się początkowo żółte , chlorotyczne plamy , później brunatniejące .
Teliospory elipsoidalne do podłużnych , umiarkowanie zwężone na przegrodzie , o wierzchołkach zaokrąglonych , podstawie lekko zwężającej się .
Mają rozmiary 27-43 × 13-20 μm .
Ściana bladożółta do bladożółtobrązowej , na bokach o grubości 1,6 μm .
