Puccinia caricina
Puccinia caricina DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożyt dwudomowy , którego żywicielami są różne gatunki porzeczek ( `` Ribes '' ) i turzyc ( `` Carex '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Ma 25 synonimów .
Morfologia i rozwój .
Cykl życiowy `` Puccinia caricina '' odbywa się na dwóch żywicielach .
Spermogonia i ecja rozwijają się na różnych gatunkach porzeczek .
Tworzą się na liściach i ogonkach liściowych , na młodych pędach i młodych owocach .
Mają kubkowaty kształt , a w miejscu ich występowania i w otoczeniu następuje chloroza .
Uredinia i telia rozwijają się na turzycach .
Jasnobrązowe uredinia tworzą się w obrębie żółtych plam na dolnej stronie liści .
Powstają w nich urediniospory z 3–4 porami rostkowymi i ścianami o grubości 1–2 µm .
Telia czarnobrązowe .
Teliospory 2–komórkowe , gładkie , prawie bez przewężenia na przegrodzie , zwężające się przy szypule o mniej więcej tej samej długości co zarodnik .
Żywiciele .
Spermogonia i ecja rozwijają się na gatunkach porzeczek ( `` Ribes '' ) : porzeczka alpejska ( `` R. alpinum '' ) , porzeczka złota ( `` R. aureum '' ) , porzeczka czarna ( `` R. nigrum '' ) , porzeczka zwyczajna ( `` R. rubrum '' ) , porzeczka krwista ( `` R. sanguineum '' ) , agrest ( `` R. uva-crispa '' ) .
Drugim żywicielem są turzyce ( `` Carex '' ) , na których rozwijają się uredinia i telia .
Stwierdzono ich występowanie na turzycy Buxbauma ( `` C. buxbaumii '' ) , turzycy obłej ( `` C. diandra '' ) , turzycy nibyciborowatej ( `` C. pseudocyperus '' ) , turzycy dzióbkowatej ( `` C. rostrata '' ) .
Na urediniach `` Puccinia caricina '' pasożytuje nadpasożyt `` Eudarluca caricis '' .
