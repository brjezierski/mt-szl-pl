Puccinia circaeae
Puccinia circaeae ( F. Strauss ) DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach czartaw ( `` Circaea '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia i rozwój .
Monofag i pasożyt jednodomowy , tzn .
że cały jej cykl życiowy przebiega na jednym tylko żywicielu .
Jest też rdzą niepełnocyklową – wytwarza tylko jeden rodzaj zarodników – teliospory .
Na czartawach występuje jeszcze drugi gatunek z rodzaju `` Puccinia '' – `` Puccinia circaeae-caricis '' .
Jest to pasożyt dwudomowy .
Na czartawach tworzy ecja i spermogonia , zaś uredinia i telia tworzy na niektórych gatunkach turzyc ( `` Carex '' ) .
Występowanie .
Występuje w Ameryce Północnej , Europie i Azji .
Zanotowano jego telia na czartawie pospolitej ( `` Circaea lutetiana '' ) , czartawie drobnej ( `` Circaea alpina '' ) , ich mieszańcu `` Circaea lutetiana × alpina '' i `` Circaeae erubescens '' .
