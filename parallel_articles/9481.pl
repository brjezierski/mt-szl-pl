Puccinia cnici
Puccinia cnici H. Mart .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący głównie na różnych gatunkach ostrożni ( `` Cirsium '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia .
Jest rdzą jednodomową , tzn , że cały cykl rozwojowy odbywa się na jednym żywicielu .
Spermogonia powstają przeważnie na górnej stronie liści , ecja przeważnie na dolnej stronie , w obrębie chlorotycznych plam .
Pomarańczowo-żółte zarodniki powstają w nich pojedynczo , wydostają się na zewnątrz przez ostiolum w owocnikach .
Uredinia i telia tworzą się na obydwu stronach liści .
Uredinia rdzawe , powstają w nich urediniospory o średniej wielkości około 30 μm z trzema porami rostkowymi widocznymi jako białe plamy .
Telia niewielkie , ciemnobrązowe .
Powstaje w nich masa czarnych , dwukomórkowych teliospor o bardzo drobno brodawkowanej powierzchni .
Pory rostkowe znajdują się w dolnej części teliospor , w pobliżu przegrody dzielącej ich komórki .
Występowanie .
`` Puccinia cnici '' jest szeroko rozprzestrzeniony w Ameryce Północnej ( USA i Kanada ) i Europie .
Poza tym podano jej występowanie w Chinach , Chile i na Nowej Zelandii .
W Polsce występuje .
Żywicielami `` Puccinia cnici '' są : `` Carduus crispus , Cirsium arvense , Cirsium lanceolatum , Cirsium eriophorum , Cirsium vulgare '' , `` Cirsium boujartii '' , `` Cirsium ferox , Cirsium furiens , Cirsium italicum , Cirsium richterianum , Cirsium serulatum , Picnomon acarna '' .
