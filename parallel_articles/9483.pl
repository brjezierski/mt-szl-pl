Charakterystyka .
`` Puccinia dioicae '' jest pasożytem jednodomowym , tzn .
cały jej cykl rozwojowy odbywa się na jednym żywicielu .
Spermogonia tworzą się na obydwu stronach blaszki liściowej , ecja tylko na dolnej stronie blaszki .
Mają białe , wygięte na zewnątrz perydium .
Zarodniki w masie pomarańczowe .
Występuje w Europie , Azji , Ameryce Północnej i Południowej .
Jest oligofagiem pasożytującym na licznych gatunkach roślin .
Stwierdzono jego występowanie na `` Centaurea benedicta '' , `` Cirsiu '' m ( `` arvense , canum , carniolicum , dissectum , eriophorum , erisithales , heterophyllum , monspessulanum , oleraceum , palustre , rivulare , spinosissimum , tartaricum , tuberosum , vulgare '' ) .
W Polsce podano jej występowanie także na innych gatunkach roślin : stokrotnica górska , jastrun właściwy , mniszek pospolity , chaber łąkowy , chaber nadreński i różne gatunki turzyc .
