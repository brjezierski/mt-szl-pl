Puccinia dioicae
Puccinia dioicae Magnus – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodziny astrowatych ( `` Asteraceae '' ) .
Wywołuje u niej chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Takson ten opisał Paul Wilhelm Magnus w 1877 r .
