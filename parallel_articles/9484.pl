Charakterystyka .
`` Puccinia fergussonii '' jest pasożytem jednodomowym , tzn .
cały jej cykl rozwojowy odbywa się na jednym żywicielu .
Jest też rdzą niepełnocyklową , wytwarza tylko jeden rodzaj zarodników – teliospory .
Powstają w ciemnobrązowych teliach , które rozwijają się pod skórką rośliny żywicielskiej .
Powstają w gęstych skupiskach zarówno na blaszce liściowej , jak i na ogonku .
Skórka pęka dopiero pod koniec ich dojrzewania .
Z teliów wydostają się wówczas 2-komórkowe teliospory o wymiarach 12–21 × 26 × 46 µm , gładkie , na trzonku o długości ok. 30 µm .
Występuje w Europie , Ameryce Północnej i w Japonii .
Jest monofagiem pasożytującym tylko na fiołkach .
W Polsce notowana na fiołku błotnym ( `` Viola palustris '' ) i fiołku torfowym ( `` Viola epipsila '' ) .
