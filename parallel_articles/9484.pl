Puccinia fergussonii
Puccinia fergussonii Berk .
& amp ; Broome – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju fiołek ( `` Viola '' ) .
Wywołuje u niej chorobę zwaną rdzą .
Systematyka i nazewnictwo .
