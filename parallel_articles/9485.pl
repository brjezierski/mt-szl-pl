Puccinia glechomatis
Puccinia glechomatis DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju bluszczyk ( `` Glechoma '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Charakterystyka .
Jest rdzą jednodomową , tzn , że cały cykl rozwojowy odbywa się na jednym żywicielu i rdzą niepełnocyklową wytwarzającą tylko jeden rodzaj zarodników – dwukomórkowe teliospory .
Pora rostkowa znajduje się w komórce trzonkowej , tuż przy sepcie oddzielającej komórki .
Komórka wierzchołkowa posiada krótki , masywny dzióbek .
Jest szeroko rozprzestrzeniona w Europie .
Poza tym zanotowano jej występowanie w Japonii i w USA .
W Polsce występuje .
Jej żywicielami są bluszczyk kurdybanek ( `` Glehoma hederacea '' ) i bluszczyk kosmaty ( `` Glechoma hirsuta '' ) .
Prowadzi się badania nad możliwością wykorzystania `` Puccinia glechomatis '' do biologicznego zwalczania bluszczyka kurdybanka , który jest uciążliwym chwastem w uprawach rolniczych .
