Charakterystyka .
Większość gatunków rdzy to pasożyty dwudomowe , których rozwój przebiega na dwóch roślinach żywicielskich .
`` Puccinia glomerata '' należy do mniej licznej grupy pasożytów jednodomowych – cały jej cykl rozwojowy odbywa się wyłącznie na jednej roślinie – są to różne gatunki starców .
Objawem porażenia są rdzawe brodawki na dolnej stronie liści .
Jest rdzą niepełnocyklową .
Nie są znane sporydia , ecja ani uredinia .
Telia powstają licznie na dolnej stronie liści .
Teliospory elipsoidalne o barwie od złotej do bordowej , dwukomórkowe .
Mają rozmiar 23–41 × 14–27 μm .
Osadzone są na trzoneczkach , które podczas odrywania się zarodników odpadają .
