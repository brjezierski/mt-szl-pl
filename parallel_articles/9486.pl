Puccinia glomerata
Puccinia glomerata Grev .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Pasożyt , fitopatogen roślin z rodzaju starzec ( `` Senecio '' ) , wywołujący u nich chorobę o nazwie rdza .
W Ameryce Północnej i Europie jest pospolity i szeroko rozprzestrzeniony .
Systematyka i nazewnictwo .
