Puccinia helianthi
Puccinia helianthi Schwein .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożytujący na kilku rodzajach roślin z rodziny astrowatych : słoneczniku ( `` Helianthum '' ) , rzepieniu ( `` Xanthium '' ) i `` Iva '' .
Wywołuje u nich chorobę zwaną rdzą ( u słonecznika jest to rdza słonecznika ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonim : `` Dicaeoma helianthii '' ( Schwein . )
Kuntze .
Morfologia i rozwój .
Jest pasożytem jednodomowym , tzn .
cały jego cykl rozwojowy odbywa się na jednym żywicielu .
Jest wąskim oligofagiem , występującym na zaliczanych do kilku rodzajów gatunkach roślin z rodziny astrowatych ( `` Asteraceae '' ) .
Jest rdzą pełnocyklową , wytwarzającą wszystkie 5 typowych dla rdzowców rodzajów zarodników .
W okresie wegetacyjnym na porażonych roślinach wytwarzane są spermacja , ecjospory , urediniospory i teliospory , od jesieni do wiosny na obumarłych resztkach porażonych roślin wytwarzane są bazydiospory , które wiosną dokonują infekcji pierwotnej porażając młode rośliny .
Latem urediniospory dokonują infekcji wtórnych rozprzestrzeniając chorobę .
Ecja , uredinia i telia występują przeważnie na dolnej stronie liści porażonych roślin .
Ecja kubkowate , uredinia jasnobrązowe , telia ciemnobrązowe .
Urediniospory o 2 porach rostkowych .
Teliospory dwukomórkowe o trwałej szypułce długości do 120 μm .
Występowanie .
Gatunek rozprzestrzeniony na całym świecie .
Jego żywicielami są : słonecznik zwyczajny ( `` Helianthus annuus '' ) , słonecznik ogórkolistny ( `` Helianthus cucumerifolius '' ) , `` Helianthus atrorubens '' , `` Helianthus giganteus '' , `` Helianthus debilis '' , słonecznik bulwiasty ( `` Helianthus tuberosus '' ) , `` Iva xanthifolia '' , rzepień pospolity ( `` Xanthium strumarium '' ) .
