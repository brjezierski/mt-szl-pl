Rozwój .
`` Puccinia hordei '' jest pasożytem dwudomowym , znaczy to , że jej rozwój odbywa się na dwóch różnych żywicielach .
Jest też pasożytem obligatoryjnym , czyli może rozwijać się tylko na żywych organizmach i rdzą pełnocyklową – wytwarza wszystkie typowe dla rdzy rodzaje zarodników .
Żywicielami ecjalnymi są różne gatunki z rodzaju śniedek ( `` Ornithogalum '' ) , `` Leopoldia '' i `` Dipcadi '' .
Powstają na nich spermogonia wytwarzające spermacja i ecja wytwarzające ecjospory .
Rozsiewane przez wiatr ecjospory dokonują na jęczmieniu infekcji pierwotnej .
Przy sprzyjającej pogodzie już po 8 dniach na liściach jęczmienia z ecjospor rozwijają się strzępki tworzące uredinia z urediniosporami , które rozsiewane przez wiatr dokonują infekcji wtórnej rozprzestrzeniając chorobę .
