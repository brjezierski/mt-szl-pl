Puccinia hordei
Puccinia hordei G.H .
Otth – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
U jęczmienia wywołuje chorobę zwaną rdzą jęczmienia .
`` Puccinia hordei '' jest szeroko rozprzestrzeniona we wszystkich rejonach uprawy jęczmienia na świecie .
