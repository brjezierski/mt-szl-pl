Puccinia horiana
Puccinia horiana Henn .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na chryzantemach ( `` Dendranthema '' ) .
Wywołuje u nich chorobę o nazwie biała rdza chryzantemy .
W Polsce jest organizmem kwarantannowym .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Biologia .
Jest pasożytem jednodomowym , tzn .
cały jego cykl życiowy odbywa się na jednym żywicielu .
Jest rdzą niepełnocyklową , wytwarzająca tylko dwa rodzaje zarodników : teliospory i bazydiospory .
Bazydiospory kiełkują w temperaturze 4 do 23° C. Uwalniają się w temperaturze 4 do 23° C , optymalna wynosi 17° C. Wiatr może je przenosić na odległość do 700 m i więcej , ale może to następować tylko przy wilgotności powietrza powyżej 90 % , są bowiem bardzo wrażliwe na przesuszenie .
Mogą kiełkować w zakresie temperatur 17–24° C na obydwu powierzchniach blaszki liściowej .
Niezbędny do tego czas zwilżenia wynosi 5 godz .
