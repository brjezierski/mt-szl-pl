Adinol
Adinol , adinolit – rodzaj skały metamorficznej .
Składa się głównie z albitu i kwarcu ( drobnokrystalicznego ) .
Adinol ma barwę jasnoszarą , jest twardy , ma przełam muszlowy .
Powstaje na skutek metamorfozy skał osadowych , zazwyczaj ilastych , pod wpływem intruzji skał zasadowych lub gorących roztworów proweniencji magmowej , bogatych w sole sodowe i krzemionkę .
Tworzy się w spągu skał wylewnych lub na kontaktach z intruzjami hipabysalnymi .
W Polsce występuje w Sudetach , Karpatach i Górach Świętokrzyskich .
