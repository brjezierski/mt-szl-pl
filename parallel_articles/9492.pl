Puccinia lapsanae
Puccinia lapsanae Fuckel – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju łoczyga ( `` Lapsana '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Takson ten opisał w 1860 r. Karl Wilhelm Gottlieb Leopold Fuckel .
Synonimy :
Morfologia i rozwój .
`` Puccinia lapsanae '' jest pasożytem jednodomowym , tzn .
cały jej cykl rozwojowy odbywa się na jednym żywicielu .
Jest rdzą pełnocyklową , wytwarzającą wszystkie typowe dla rdzy rodzaje zarodników .
