Puccinia magnusiana
Puccinia magnusiana Körn .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny , pasożytujący na jaskrach ( `` Ranunculus '' ) oraz lasecznicach ( `` Arundo '' ) i trzcinie pospolitej ( `` Phragmites australis '' ) .
U porażonych roślin wywołuje chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Gatunek ten po raz pierwszy opisał Friedrich August Körnicke w 1876 r ..
Synonimy :
Morfologia i rozwój .
Jest pasożytem dwudomowym , którego cykl życiowy odbywa się na dwóch gatunkach żywicieli .
Na jaskrach tworzy spermogonia i ecja , na trzcinie i lasecznicy uredinia i telia .
Strzępki rozrastają się między komórkami porażonych roślin , do ich wnętrza zapuszczając tylko ssawki pobierające z rośliny wodę z substancjami pokarmowymi .
Zarodniki tworzą się pod skórką roślin , po dojrzeniu nabrzmiewają , co powoduje pękanie skórki rośliny i wydostanie się zarodników na zewnątrz .
Żółte ecja tworzą się na dolnej stronie liści jaskrów , na ogonkach liściowych i na łodygach .
