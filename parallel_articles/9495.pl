Morfologia .
Jest rdzą jednodomową , tzn , że cały jej cykl życiowy odbywa się na jednym żywicielu , i niepełnocyklową , wytwarzającą tylko dwa rodzaje zarodników : bazydiospory i teliospory .
Latem na dolnej stronie porażonych liści roślin , a także na ich ogonkach i na łodygach , tworzą się grudkowate telia o kasztanowej barwie .
Powstają w nich zazwyczaj dwukomórkowe , rzadziej 3–4-komórkowe , podłużne teliospory o rozmiarach 12–26 μm × 35–75 μm .
Na obydwu końcach zwężają się , w nasadzie posiadają trzonek do dwóch razy dłuższy od zarodnika .
Są gładkie , mają żółto–cynamonowo-brązową ścianę o grubości 1–4 μm na boku i 5–10 μm na szczycie teliospory .
Później telia stają się białe – to wynik tworzenia się na nich bazydiospor .
Występowanie .
`` Puccinia malvacearum '' jest rozprzestrzeniony na całym świecie ( występuje także w Polsce ) .
Żywicielami są gatunki należące do rodzajów : malwa ( `` Alcea '' ) , ślaz ( `` Malva '' ) , ślazownik ( `` Sidalcea '' ) , ślazówka ( `` Lavatera '' ) , ślęzawa ( `` Malope '' ) , `` Palaua '' .
