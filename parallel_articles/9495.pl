Puccinia malvacearum
Puccinia malvacearum Bertero ex Mont .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na wielu rodzajach roślin w rodzinie ślazowatych ( `` Malvaceae '' ) .
Wywołuje u nich chorobę zwaną rdzą malwy lub rdzą ślazu .
