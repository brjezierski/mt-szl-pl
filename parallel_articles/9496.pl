Puccinia menthae
Puccinia menthae Pers .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny będący pasożytem wielu roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy nazwy naukowej :
Rozwój .
Jest pasożytem , jak wszystkie gatunki z rodzaju rdza .
Jest rdzą pełnocyklową , tzn .
wytwarza wszystkie właściwe dla rdzy rodzaje zarodników .
Jest pasożytem jednodomowym , tzn .
cały jej cykl rozwojowy odbywa się na jednym żywicielu .
Morfologia .
Pyknidia rozwijają się na liściach lub łodygach , zazwyczaj w niewielkich grupach , razem z ecjami .
