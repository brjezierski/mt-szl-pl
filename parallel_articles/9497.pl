Charakterystyka .
`` Puccinia obscura '' jest pasożytem dwudomowym , tzn .
jej cykl życiowy odbywa się na dwóch gatunkach żywicieli .
Spermogonia rozwijają się na liściach stokrotki , tworząc na nich zwarte , okrągłe skupiska .
Ecja tworzą się w obrębie chlorotycznych i nieco nabrzmiałych plam , również na liściach stokrotki , zarówno na ich górnej , jak dolnej powierzchni .
Drugim żywicielem są różne gatunki kosmatek ( `` Luzula '' ) .
W obrębie przebarwionych plam na górnej stronie ich liści powstają jasnobrązowe Uredinia i ciemnobrązowe telia ( te są rzadko spotykane ) .
Urediniospory kolczaste , z dwoma porami rostkowymi , każda otoczony gładką strefą .
Teliospory 2-komórkowe na trwałym trzonku o długości ok. 30 µm .
Ich wierzchołkowa część górnej komórki ma silnie pogrubioną ścianę komórkową .
`` Puccinia obscura '' występuje w licznych krajach Europy , w Ameryce Północnej , podano jej występowanie także w Ameryce Południowej , Afryce ( Algieria ) , Korei , Japonii i na Nowej Zelandii .
Występuje na stokrotce pospolitej ( `` Bellis perennis '' ) , oraz różnych gatunkach kosmatek ( `` Luzula '' ) : kosmatka polna ( `` Luzula campestris '' ) , kosmatka gajowa ( `` Luzula luzuloides '' ) , kosmatka licznokwiatowa ( `` Luzula multiflora '' ) , kosmatka blada ( `` Luzula pallescens '' ) , kosmatka owłosiona `` Luzula pilosa '' i kosmatka olbrzymia ( `` Luzula sylvatica '' ) .
