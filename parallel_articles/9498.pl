Puccinia pimpinellae
Puccinia pimpinellae ( F. Strauss ) Link – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący niemal wyłącznie na roślinach z rodzaju biedrzeniec ( `` Pimpinella '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' } .
Takson ten opisał w 1811 r. Friedrich Karl Joseph von Strauss nadając mu nazwę `` Uredo pimpinellae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Johann Heinrich Friedrich Link w 1824 r .
