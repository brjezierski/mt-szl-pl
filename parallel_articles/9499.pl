Puccinia poarum
Puccinia poarum Nielsen – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Jest szeroko rozprzestrzeniony w Ameryce Północnej i Europie , występuje także w Azji i Australii .
Grzyb mikroskopijny , pasożyt obligatoryjny wielu gatunków roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
