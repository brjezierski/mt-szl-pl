Charakterystyka .
Jest to pasożyt dwudomowy , czyli taki , który dla pełnego cyklu rozwojowego potrzebuje dwóch gatunków roślin żywicielskich : część rozwoju odbywa na liściach podbiału pospolitego , część – na licznych gatunkach roślin z rodziny wiechlinowatych ( `` Poaceae '' ) .
Podbiał pospolity zakażany jest przez zarodniki zwane sporydiami , które wnikają przez aparaty szparkowe do tkanek jego liści .
Rozwija się z nich grzybnia odżywiająca się tkankami gospodarza i powodująca ich nekrozę .
W okresie od maja do czerwca na dolnej stronie liści powstają rdzawe lub pomarańczowo-czerwone okrągławe plamy o średnicy 1-2 cm .
Znajduje się w nich około 20-30 ecjów tworzących pierścień .
Czasami powstaje wokół nich fioletowy pierścień , a w środku plamy centralny otwór .
Na górnej powierzchni liścia nad ecjami powstaje żółta plama ze spermogoniami .
Gatunki podobne .
Na liściach podbiału występuje także grzyb `` Coleosporium tussilaginis '' , który również na ich dolnej stronie tworzy pomarańczowe plamy .
Pojawiają się one jednak w sezonie wegetacyjnym później niż `` Puccinia poarum '' , nie są tak okrągłe i nie tworzą tak dobrze wyodrębnionych klastrów , lecz zazwyczaj duże plamy , często zlewające się .
