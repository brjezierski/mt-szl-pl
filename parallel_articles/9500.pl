Puccinia polygoni-amphibii
Puccinia polygoni-amphibii Pers .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju bodziszek ( `` Geranium '' ) i rdest ( `` Polygonum , Persicaria '' ) .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
