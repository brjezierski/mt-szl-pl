Morfologia i rozwój .
`` Puccinia polygoni-amphibii '' jest pasożytem dwudomowym , tzn. , że jego cykl życiowy odbywa się na dwóch żywicielach .
Spermogonia i ecja rozwijają się na liściach bodziszków ( `` Geranium '' ) .
Spermogonia tworzą się na obydwu stronach blaszki liściowej , są nieliczne i częściowo zagłębione w tkance liścia .
Ecja powstają głównie na dolnej stronie liścia , są cylindryczne , z wąskim zakrzywionym brzegiem podzielonym na małe segmenty .
Tworzą koncentryczne kręgi na intensywnie zaczerwienionych częściach liści .
Brązowe uredinia i czarnobrązowe telia rozwijają się na niektórych gatunkach rdestów ( `` Polygonum '' ) , przeważnie na dolnej stronie liści .
Urediniospory z 2 porami rostkowymi powyżej równika .
Teliospory dwukomórkowe , ściana górnej komórki jest silnie pogrubiona .
Mają trwały trzonek o barwie od szklistej do żółtawobrązowej , mniej więcej tej samej długości co zarodnik .
Występowanie .
`` Puccinia polygoni-amphibii '' występuje na wszystkich kontynentach poza Antarktydą .
W Polsce opisano występowanie na bodziszku łąkowym ( `` Geranium pratense '' ) , rdeście ziemnowodnym ( `` Persicaria amphibia '' ) i rdeście szczawiolistnym ( `` Polygonum lapathifolium '' ) .
