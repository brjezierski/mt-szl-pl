Puccinia pulverulenta
Puccinia pulverulenta Grev .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb pasożytniczy , wywołujący u roślin chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1824 r. Robert Kaye Greville .
Synonimy :
Morfologia i rozwój .
Jest rdzą pełnocyklową , tzn .
tworzy wszystkie rodzaje zarodników typowych dla rdzowców .
Na dolnej stronie porażonych liści ( przy dużym porażeniu także na górnej ) tworzą się ecja , często w dużej ilości , oraz rozproszone wśród nich żółte spermogonia .
Perydium białe , miseczkowate , o odginających się brzegach .
Ecjospory długości 16–26 µm i średnicy 13–22 µm , kuliste do cylindryczno-elipsoidalnych , pomarańczowe , cienkościenne , delikatnie brodawkowate .
Uredinia na dolnej stronie liści , rozproszone lub czasami zlewające się w grupy , o mączystym wyglądzie , kasztanowobrązowe .
