Puccinia punctata
Puccinia punctata Link – gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący głównie na roślinach z rodzaju przytulia ( `` Galium '' .
Wywołuje u nich chorobę zwaną rdzą .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Morfologia .
Jest pasożytem jednodomowym , tzn , że jego cykl życiowy odbywa się na jednym żywicielu .
Wytwarza na nim spermogonia , ecja , uredinia i telia .
Grzybnia rozwija się między komórkami porażonej rośliny .
Na porażonych liściach tworzą się pomarańczowe plamy .
