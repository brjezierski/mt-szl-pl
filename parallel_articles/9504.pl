Morfologia i rozwój .
`` Puccinia saniculae '' jest pasożytem jednodomowym , tzn .
że jego cykl życiowy odbywa się na jednym żywicielu .
Ecja , uredinia i telia tworzą się na dolnej stronie liści .
Ecja są żółte z białymi brzegami , uredinia jasnobrązowe , a telia ciemnobrązowe .
Powstają w obrębie fioletowych plam na liściach i na łodydze .
Urediniospory z dwoma , czasami z trzema równikowymi porami rostkowymi .
Teliospory dwukomórkowe z trzonkami .
Występowanie .
Opisano występowanie `` Puccinia saniculae '' w Europie , na wschodnim wybrzeżu Ameryki Północnej i w Japonii .
W Polsce opisano występowanie na żankielu zwyczajnym ( `` Sanicula europaea '' ) , w Europie ponadto na `` Sanicula azorica '' .
