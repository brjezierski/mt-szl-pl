Puccinia saniculae
Puccinia saniculae Grev .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na żankielu zwyczajnym ( `` Sanicula europaea '' ) .
Wywołuje u niego chorobę zwaną rdzą .
Systematyka i nazewnictwo .
