Puccinia sorghi
Puccinia sorghi Schwein .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzajów szczawik , kukurydza , sorgo , `` Euchlaena '' .
Na kukurydzy wywołuje chorobę o nazwie rdza kukurydzy .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1832 r. Lewis David von Schweinitz na liściach kukurydzy ( `` Zea '' ) i sorga ( `` Sorghum '' ) w stanie Dakota Południowa w USA .
Synonimy :
Morfologia i cykl życiowy .
Jest rdzą pełnocyklową i pasożytem dwudomowym , tzn , że jego cykl życiowy odbywa się na dwóch gatunkach żywicieli .
Spermogonia i ecja rozwijają się na różnych gatunkach szczawików , a uredinia i telia na kukurydzy , sorgu , `` Euchlaena '' .
Spermogonia ( pyknidia ) po 6 w kolistych skupiskach o średnicy do 0,5 mm .
Ecja pojawiają się dopiero na opadłych , porażonych liściach , otaczając sporogonia strefą o szerokości do 2 mm .
Są miseczkowate , mają średnicę 0,15–0,2 mm .
