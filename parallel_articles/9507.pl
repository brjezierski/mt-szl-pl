Puccinia tanaceti
Puccinia tanaceti Schwein .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na gatunkach roślin z rodzaju wrotycz ( `` Tanacetum '' ) .
Wywołuje u nich chorobę o nazwie rdza .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Cykl życiowy i morfologia .
Pasożyt jednodomowy i wąski monofag , którego jedynym żywicielem są rośliny należące do rodzaju wrotycz .
Rdza niepełnocyklowa , wytwarzająca tylko 2 rodzaje zarodników : urediniospory i teliospory .
Na obydwu stronach liści porażonych roślin tworzy uredinia i telia .
Uredinia jasnobrązowe , powstające w nich urediniospory mają powierzchnię drobnokolczastą i 3 pory rostkowe , każda z hialinowymi , gładkimi brodawkami .
Telia czarnobrązowe , teliospory gładkie , dwukomórkowe z długim , trwałym , hialinowym trzonkiem .
Występowanie .
Gatunek szeroko rozprzestrzeniony .
Podano liczne stanowiska w Ameryce Północnej i Europie i mniej liczne w Ameryce Południowej , Azji , Afryce i na Nowej Zelandii .
Wśród jego żywicieli znane są tylko 2 gatunki wrotyczy : wrotycz pospolity ( `` Tanacetum vulgare '' ) i wrotycz maruna ( `` Tanacetum parthenium '' ) .
Badania przeprowadzone w USA potwierdziły wąski monofagizm rdzy `` Puccinia tanaceti '' .
Patogena tego udało się skutecznie zaszczepić na wrotyczu pospolitym , nie powiodło się to natomiast na blisko z nim spokrewnionych roślinach z plemienia `` Anthemidae '' ( rodzina astrowatych ) : `` Leucanthemum maximum '' , `` Leucanthemum vulgare '' , `` Artemisia absinthium '' i `` A. tridentata '' .
Występujące na tych roślinach gatunki opisane jako `` P. tanaceti '' to w istocie inne gatunki rodzaju `` Puccinia '' .
