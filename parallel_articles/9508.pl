Puccinia variabilis
Puccinia variabilis Grev .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Grzyb mikroskopijny pasożytujący na roślinach z rodzaju mniszek ( `` Taraxacum '' ) .
Wywołuje u nich chorobę o nazwie rdza .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Synonimy :
Cykl życiowy i morfologia .
Pasożyt jednodomowy i wąski monofag , którego jedynym znanym żywicielem są niektóre gatunki mniszka .
Rdza niepełnocyklowa , wytwarzająca ecja , uredinia i telia .
Ecja w małych grupkach w obrębie plam , głównie na górnej powierzchni liści .
Uredinia w rozproszeniu na obydwu stronach liści , ale głównie na dolnej .
Są żółtobrązowe , grudkowate , koliste , o wymiarach 0,09–0,1 × 0,3–0,4 mm .
Urediniospory jajowate do prawie kulistych , bladobrązowe do żółtobrązowych , o wymiarach 17–26 × 23–28 μm ( średnio 22 × 26 μm ) , z 2–3 porami rostkowymi .
Powierzchnia drobnokolczasta , ściana o grubości 1–2 μm , trzonek hialinowy , nietrwały .
Teliospory dwukomórkowe , elipsoidalne do jajowatych , na obydwu końcach zaokrąglone , nieco zwężone na przegrodzie .
Mają kszatanowobrązową barwę , wymiary 19–26 × ( 23– ) 25–36 μm ( średnio 24 × 32 μm ) ; ścianę o grubości 1,5–3 μm , powierzchnię drobnobrodawkowatą , prawie gładką , 2 pory rostkowe z hialinową brodawką i hialinowy trzonek o wymiarach 7–9 × 17–35 μm .
Występowanie .
Podano liczne stanowiska w Ameryce Północnej ( USA i Kanada ) , w Europie , na Nowej Zelandii i w niektórych rejonach Azji ( Pakistan ) .
Wśród jego żywicieli znane są tylko trzy gatunki mniszka : mniszek błotny ( `` Taraxacum palustre '' ) , mniszek pospolity ( `` Taraxacum vulgaris '' ) i `` Taraxacum silvestris '' .
