Puccinia violae
Puccinia violae ( Schumach . )
DC .
– gatunek grzybów z rodziny rdzowatych ( `` Pucciniaceae '' ) .
Jest szeroko rozprzestrzeniony w Ameryce Północnej i Europie , występuje także w Ameryce Południowej , Azji , Australii i Nowej Zelandii .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Puccinia , Pucciniaceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1803 r. Heinrich Christian Friedrich Schumacher nadając mu nazwę `` Aecidium violae '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu w 1815 r. Augustin Pyramus de Candolle , przenosząc go do rodzaju `` Puccinia '' .
