Pucciniastrum epilobii
Pucciniastrum epilobii ( Pers . )
G.H .
Otth – gatunek grzybów z typu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny pasożytujący na jodłach i roślinach z rodziny wiesiołkowatych .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pucciniastrum , Pucciniastraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1801 r. Christiaan Hendrik Persoon nadając mu nazwę `` Uredo pustulata a epilobii '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Gustav Heinrich Otth w 1861 r .
Cykl życiowy i morfologia .
Jest to pasożyt dwudomowy i rdza pełnocyklowa , wytwarzająca wszystkie typowe dla rdzowców rodzaje zarodników .
Na jodłach ( `` Abies '' ) powstają spermogonia i ecja ( jodła jest żywicielem ecjalnym ) , na roślinach z rodziny wiesiołkowatych ( `` Onagraceae '' ) uredinia i telia ( są to żywiciele telialni ) .
Spermogonia i ecja powstają pod skórką liści .
Cylindryczne ecja o wysokości 1 mm tworzą się w dwóch rzędach .
Ecjospory brodawkowane , ale z jednej strony z gładką plamką .
