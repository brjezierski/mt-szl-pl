Pucciniastrum sparsum
Pucciniastrum sparsum ( G. Winter ) E. Fisch .
– gatunek grzybów z typu rdzowców ( `` Pucciniales '' ) .
Grzyb mikroskopijny pasożytujący na świerku pospolitym i mącznicach .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pucciniastrum , Pucciniastraceae , Pucciniales , Incertae sedis , Pucciniomycetes , Pucciniomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy zdiagnozował go w 1881 r. Heinrich Georg Winter , nadając mu nazwę `` Melampsora sparsa '' .
Obecną , uznaną przez Index Fungorum , nazwę nadał mu Eduard Fisch w 1904 r .
Synonimy :
Cykl życiowy i morfologia .
Jest to pasożyt dwudomowy i rdza pełnocyklowa , wytwarzająca wszystkie typowe dla rdzowców rodzaje zarodników .
Na świerku pospolitym ( `` Picea abies '' ) powstają spermogonia i ecja ( świerk jest żywicielem ecjalnym ) , na roślinach z rodzaju mącznica ( `` Arctostaphylos '' ) uredinia i telia ( są to żywiciele telialni ) .
