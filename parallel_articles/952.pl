Adolf Joszt
Adolf Joszt ( ur .
17 czerwca 1889 we Lwowie , zm .
16 marca 1957 w Gliwicach ) – polski profesor technologii chemicznej , prekursor biotechnologii i ochrony środowiska .
Życiorys .
Był synem Franciszka i Marii z Gerynowiczów .
W 1907 ukończył z odznaczeniem C. K. IV Gimnazjum we Lwowie .
W 1911 ukończył studia na Wydziale Chemicznym Politechniki Lwowskiej .
W 1918 roku walczył w obronie Dublan ( obsługiwał karabin maszynowy ) , a następnie w obronie Lwowa , był wojskowym pirotechnikiem w Krakowie i Lwowie .
W listopadzie 1919 został urlopowany , a następnie zwolniony z wojska .
W 1919 uzyskał doktorat , a w 1920 habilitował z pracy o amylokoagulazie .
Pełnił funkcję kierownika Stacji fermentacyjnej w Akademii Rolniczej w Dublanach .
W 1923 został profesorem nadzwyczajnym , a w 1931 profesorem zwyczajnym technologii chemicznej przemysłu rolniczego i mikrobiologii technicznej Politechniki Lwowskiej .
W latach 1924–1925 był dziekanem Wydziału Lasowego , a w latach 1930–1931 dziekanem Wydziału Chemicznego Politechniki Lwowskiej .
W maju 1936 został rektorem Politechniki Lwowskiej wybrany na okres trzech lat .
