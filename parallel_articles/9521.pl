Puchar Konfederacji w piłce nożnej 2017
Puchar Konfederacji w piłce nożnej 2017 rozgrywany był w dniach 17 czerwca – 2 lipca 2017 w Rosji .
Turniej był próbą generalną przed odbywającymi się w tym kraju w 2018 roku Mistrzostwami Świata .
Mecz otwarcia oraz finał odbyły się w Petersburgu .
Ponadto mecze turniejowe odbywały się w Moskwie , Kazaniu i Soczi .
Puchar Konfederacji w Rosji był ostatnim turniejem w historii tych rozgrywek .
