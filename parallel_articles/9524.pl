Pula ( stacja kolejowa )
Pula ( chorwacki : `` Željeznički kolodvor u Puli '' ) – stacja kolejowa w Puli , w żupanii istryjskiej , w Chorwacji .
Wybudowana w 1876 roku .
Budynek dworca znajduje się u podnóża Monte Ghiro na brzegu Zatoki .
Dworzec łączy funkcje pasażerskie i towarowe .
W ostatnich latach ruch na stacji zmniejszył się ze względu na odcięte Istrii od reszty sieci kolejowej Chorwacji , które miały miejsce po uzyskaniu niepodległości Chorwacji i Słowenii .
