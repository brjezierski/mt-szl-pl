Pulsoksymetr
Pulsoksymetr – urządzenie elektroniczne służące do nieinwazyjnego pomiaru saturacji krwi , wykorzystujące pomiar pochłaniania przez tkanki promieniowania o dwóch różnych długościach fal metodą pulsoksymetrii .
Zasada działania .
Działa na zasadzie pomiaru pochłaniania przez czerwone krwinki w naczyniach włosowatych promieniowania o dwóch różnych długościach fali – czerwonego i podczerwonego .
Mierzony sygnał składa się z dwóch składowych : stałej i zmiennej ( pulsującej ) .
Składowa zmienna opisuje absorbancję pulsującej krwi tętniczej .
Na podstawie pomiaru oblicza się stopień nasycenia hemoglobiny tlenem ( SpO2 ) .
Pulsoksymetr mierzy też tętno .
