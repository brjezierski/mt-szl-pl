Adorf/Vogtl .
Adorf/Vogtl. , Adorf/Vogtland – miasto w Niemczech , w kraju związkowym Saksonia , w powiecie Vogtland , w regionie Vogtland .
Liczy 5480 mieszkańców ( 2009 ) .
W latach 1697–1706 i 1709–1763 miasto wraz z Elektoratem Saksonii było połączone unią z Polską , a w latach 1807–1815 wraz z Królestwem Saksonii unią z Księstwem Warszawskim .
Transport .
W mieście krzyżują się drogi krajowe B92 i B283 .
W mieście znajduje się stacja kolejowa Adorf ( Vogtl ) .
