Skórniczka kasztanowobrązowa
Skórniczka kasztanowobrązowa ( `` Punctularia strigosozonata '' ( Schwein . )
P.H.B .
Talbot ) – gatunek grzybów z rodziny `` Punctulariaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Punctulariaceae , Corticiales , Incertae sedis , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten opisał w 1832 r. Lewis David von Schweinitz nadając mu nazwę `` Merulius strigonozonatus '' .
Obecną , uznaną przez Index Fungorum nazwę , nadał mu Patrick Henry Brabazon Talbot w 1958 r .
Ma 22 synonimy .
Niektóre z nich :
Polską nazwę zaproponował Władysław Wojewoda w 2003 r .
Morfologia .
Jednoroczny , rozpostarty lub rozpostarto-odgięty .
Górna powierzchnia strefowana niewyraźnymi prążkami , jasno i ciemnobrązowa do czarnej , aksamitnie owłosiona z jaśniejszym brzegiem .
Dolna powierzchnia gładka lub nierówna ( guzkowata lub prążkowana ) , ciemnobrązowa , ciemnoczerwono-brązowa , fiołkowa , do szarej .
