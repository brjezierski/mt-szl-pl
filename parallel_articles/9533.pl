Punta del Este położone jest na niewielkim półwyspie o tej samej nazwie .
Jest on uznawany za umowną granicę między akwenem Rio de la Plata , a pełnymi wodami Oceanu Atlantyckiego .
Zasadzono na nim lasy eukaliptusów , araukarii i mimozy , wśród których powstały liczne luksusowe miejsca .
W Punta del Este często miały miejsce konferencje Organizacji Państw Amerykańskich .
