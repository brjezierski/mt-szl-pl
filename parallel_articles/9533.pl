Punta del Este
Punta del Este – miejscowość w południowym Urugwaju , w departamencie Maldonado , położona na południe od miasta Maldonado oraz około 100 km na wschód od stolicy kraju Montevideo , nad Oceanem Atlantyckim .
Popularna miejscowość uzdrowiskowa i turystyczna tego kraju .
Zamieszkuje je 7,3 tys .
