Ziemiozorek ciemnopurpurowy
Ziemiozorek ciemnopurpurowy , maczugowiec czarnoczerwonawy ( `` Geoglossum atropurpureum '' ( Batsch ) Pers . )
– gatunek grzybów z monotypowej rodziny `` Geoglossaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Geoglossum , Geoglossaceae , Geoglossales , Incertae sedis , Geoglossomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisał go w 1783 r. August Johann Georg Karl Batsch , nadając mu nazwę `` Clavaria atropurpurea '' .
Obecną nazwę nadał mu Christiaan Hendrik Persoon w 1796 r .
Ma 9 synonimów .
Niektóre z nich :
M.A .
Chmiel w 2006 r. podaje polską nazwę maczugowiec czarnoczerwonawy dla synonimu `` Thuemenidium atropurpureum '' .
Ta nazwa jest niespójna z aktualną nazwą naukową .
