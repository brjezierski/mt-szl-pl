Purzec
Purzec – wieś w Polsce położona w województwie mazowieckim , w powiecie siedleckim , w gminie Siedlce .
Leży 5 km od centrum Siedlec .
Przez Purzec przebiega droga powiatowa do wsi Strzała .
Przez wieś przepływa rzeka Liwiec wyznaczając północną granicę wsi .
Dawniej miejscowość ta znana była jako `` Purzyce '' należące do gminy Niwiska .
W 1827 roku wieś liczyła 9 domów i 77 mieszkańców , a w 1888 roku 13 domów i 113 mieszkańców .
Wierni Kościoła rzymskokatolickiego należą do parafii Świętego Józefa w Siedlcach .
W latach 1975–1998 miejscowość administracyjnie należała do województwa siedleckiego .
