Puteaux
Puteaux – miejscowość i gmina we Francji , w regionie Île-de-France , w departamencie Hauts-de-Seine .
Przez miejscowość przepływa Sekwana .
Według danych na rok 2012 gminę zamieszkiwało 44 514 osób , a gęstość zaludnienia wynosiła 13 954 osób/km² .
Wśród 1287 gmin regionu Île-de-France Puteaux plasuje się na 806. miejscu pod względem powierzchni .
