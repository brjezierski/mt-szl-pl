Adrianus Eversen
Adrianus Eversen ( ur .
13 stycznia 1818 , 1 grudnia 1897 ) – holenderski malarz miejskiej architektury .
Studiował malarstwo pod kierunkiem Cornelisa de Kruyffa .
Mieszkał i pracował w Amsterdamie .
Wystawiał również w Hadze i Leeuwarden , a także poza granicami Holandii m.in .
w Bremie , Dreźnie , Berlinie i Wiedniu .
Był członkiem stowarzyszenia Arti et Amicitiae , należał do holenderskiej elity artystycznej .
Jego głównym konkurentem był Cornelis Springer .
Prace Eversena odznaczają się topograficzną dokładnością , jednocześnie świadczą o wrażliwości autora , stanowią jego poetycką odpowiedź na atmosferę codziennego życia w holenderskich miastach .
Szczególną rolę w jego obrazach odgrywało światło , które było środkiem wyrazu i służyło budowaniu nastroju .
