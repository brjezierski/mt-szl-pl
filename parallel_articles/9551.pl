Jego historyczność potwierdza się na podstawie spójnych informacji zawartych w najwcześniejszych częściach tekstu Ewangelii i znaleziskach archeologicznych .
W Nowym Testamencie Piotr pojawia się wielokrotnie i wyraźnie we wszystkich czterech ewangeliach , a także w Dziejach Apostolskich .
Zwłaszcza Ewangelia Marka była tradycyjnie uważana za ukazującą wpływ Piotra i wspomnień naocznych świadków .
Jest on również wymieniony , pod imieniem Piotr lub Kefas , w Pierwszym Liście Pawła do Koryntian i Liście do Galatów .
