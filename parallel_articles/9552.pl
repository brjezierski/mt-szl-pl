Pęchowo
Pęchowo – wieś w Polsce położona w województwie kujawsko-pomorskim , w powiecie inowrocławskim , w gminie Złotniki Kujawskie .
W latach 1975–1998 miejscowość administracyjnie należała do województwa bydgoskiego .
Położenie .
Pęchowo usytuowane jest w pobliżu północnego skraju Wysoczyzny Kujawskiej , u podnóża krawędzi Kotliny Toruńskiej .
Miejscowość sąsiaduje od północy z Krążkowem i Palczynem , od wschodu z Tarkowem Górnym , a od południa z Lisewem Kościelnym .
Pod względem fizycznogeograficznym leży w obrębie Pojezierza Wielkopolskiego w mezoregionie Równina Inowrocławska .
Charakterystyka .
Pęchowo to wieś sołecka usytuowana w północnej części gminy Złotniki Kujawskie .
