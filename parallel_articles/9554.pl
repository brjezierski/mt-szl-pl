Pękanino ( powiat sławieński )
Pękanino ( dawniej : niem .
`` Panknin '' ) – wieś w północnej Polsce położona w województwie zachodniopomorskim , w powiecie sławieńskim , w gminie Malechowo .
W miejscowości ma remizę jednostka Ochotniczej Straży Pożarnej włączona do krajowego systemu ratowniczo-gaśniczego .
W latach 1975–1998 miejscowość administracyjnie należała do województwa koszalińskiego .
