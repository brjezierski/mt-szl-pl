Pyrenopeziza brassicae
Pyrenopeziza brassicae B. Sutton & amp ; Rawl .
– gatunek grzybów z rodziny `` Ploettnerulaceae '' .
Pasożyt roślin z rodziny kapustowatych , ale głównie rzepak , wywołujący u niego chorobę o nazwie biała plamistość liści rzepaku ( dawniej nazywana cylindrosporiozą roślin kapustowatych lub cylindrosporiozą roślin krzyżowych ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Pyrenopeziza , Ploettnerulaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy nazwy naukowej :
Anamorfą jest `` Cylindrosporium concentricum '' , dawniej uznawany za odrębny gatunek , obecnie za synonim .
Rozwój .
Na pędach zaatakowanych roślin pojawiają się koncentrycznie ułożone acerwulusy o średnicy 0,1–0,2 mm .
Powstają w nich jednokomórkowe , cylindryczne zarodniki konidialne o rozmiarach 10–16 × 3–4 μm .
Dokonują one infekcji wtórnej zarażając następne rośliny .
Roznoszone są przez rozpryskujące się na roślinie krople deszczu , zarażają więc tylko sąsiednie rośliny .
Jesienią na obumarłych resztkach zaatakowanych roślin rozwijają się apotecja , w których na drodze płciowej powstają askospory .
Apotecja mają średnicę 285 μm , a rozwijające się w nich worki mają długość 80–100 μm i szerokość 2,5–3,5 μm .
Askospory mają rozmiar 12,5–18,5 × 2,5–3 μm .
Dokonują one infekcji pierwotnej głównie jesienią oraz podczas łagodnej zimy .
Po okresie trwającym od kilku tygodni do kilku miesięcy na porażonych roślinach rozwijają się z nich acerwulusy .
Forma bezpłciowa , dawniej uważana za odrębny gatunek , jest obligatoryjnym pasożytem , forma płciowa saprotrofem .
