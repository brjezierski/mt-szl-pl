Pyriatyn
Pyriatyn ( ukr .
Пирятин ) – miasto na Ukrainie w obwodzie połtawskim , siedziba władz rejonu pyriatyńskiego .
Ośrodek przemysłu spożywczego .
Historia .
Pyriatyn otrzymał w 1592 r. z rąk króla Polski Zygmunta III Wazy magdeburskie prawa miejskie oraz herb .
Leżał w granicach województwa kijowskiego prowincji małopolskiej .
Utracony przez Polskę na rzecz Rosji w 1667 , co w 1686 potwierdził pokój Grzymułtowskiego .
W latach 1922-1991 stanowił część ZSRR .
