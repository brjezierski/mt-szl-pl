Pyrzowice
Pyrzowice – wieś sołecka w Polsce położona w województwie śląskim , w powiecie tarnogórskim , w gminie Ożarowice .
Na jej terenie ulokowany jest Międzynarodowy Port Lotniczy Katowice .
W latach 1975–1998 miejscowość należała administracyjnie do województwa katowickiego .
Położenie .
Wieś położona jest w północnej części Górnośląskiego Okręgu Przemysłowego .
Nazwa .
Nazwę miejscowości w zlatynizowanej staropolskiej formie `` Pyrzowicze '' wymienia w latach ( 1470–1480 ) Jan Długosz w księdze `` Liber beneficiorum dioecesis Cracoviensis '' .
Historia .
Wieś wzmiankowana po raz pierwszy w 1340 .
Wchodząca w granice Księstwa Siewierskiego , położona na skraju historycznej puszczy `` Smolnik '' , stanowiła własność klasztoru Norbertanek na krakowskim Zwierzyńcu .
Później przechodziła przez ręce kolejnych właścicieli , między innymi krakowskiego szlachcica Jana Twardowskiego , a w I połowie XV wieku stała się własnością Stanisława Rudzkiego herbu Pilawa .
