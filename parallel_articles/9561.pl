Pyrzyce ( przystanek kolejowy )
Pyrzyce – nieczynny przystanek kolejowy , a dawniej stacja węzłowa w Pyrzycach w województwie zachodniopomorskim , w Polsce .
Stacja w Pyrzycach powstała wraz z budową linii ze Stargardu do Kostrzyna , którą oddano do użytku 31 sierpnia 1882 .
Następnie 22 sierpnia 1898 otwarto linię do Płońska , a sześć dni później trasę do Sobieradza i dalej do Gryfina .
Jako ostatnią 8 stycznia 1899 oddano do użytku linię do Godkowa .
Jako pierwszą dla ruchu pasażerskiego zamknięto w 1954 linię do Płońska , następnie w 1992 do Godkowa , w 1996 do Gryfina , w 2000 do Głazowa .
Jako ostatnią w 2004 zamknięto trasę do Stargardu .
