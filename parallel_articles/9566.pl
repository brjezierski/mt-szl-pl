Przeszczep .
W 2005 roku w Chinach przeprowadzono pierwszą na świecie operację przeszczepienia penisa , która mogła być zakwalifikowana jako udana , ale po dwóch tygodniach pacjent zażądał usunięcia przeszczepu z powodu problemów psychologicznych swoich i jego żony .
W marcu 2015 roku wydział medyczny Uniwersytetu w Stellenbosh w Republice Południowej Afryki poinformował o przeprowadzeniu w grudniu poprzedniego roku we współpracy ze szpitalem Tygerberg w Kapsztadzie pierwszej na świecie udanej transplantacji penisa u 21-letniego pacjenta , który trzy lata wcześniej musiał przejść zabieg jego amputacji z powodu komplikacji po obrzezaniu .
W wyniku zabiegu pacjent odzyskał wszystkie funkcje w przeszczepionym organie .
