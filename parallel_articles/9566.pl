U mężczyzn przez prącie przebiega ostatni odcinek cewki moczowej , której ujście znajduje się na szczycie żołędzi prącia .
Składa się z dwóch równoległych ciał jamistych oraz ciała gąbczastego tworzącego żołądź prącia ( `` glans penis '' ) oraz tzw .
opuszkę .
Ciało gąbczaste ( `` corpus spongiosum penis '' ) osłania również biegnącą przez prącie cewkę moczową .
Prącie charakteryzuje się zdolnością do erekcji .
Wyróżniamy nasadę oraz część ruchomą .
Nasada przytwierdzona jest odnogami ciał jamistych do kości łonowych i kulszowych .
Część ruchoma prącia zakończona jest żołędzią .
Skóra prącia leży na luźnej tkance podskórnej i dlatego łatwo zsuwa się w trakcie erekcji .
Ponad żołędzią skóra tworzy zdwojony fałd – napletek .
Prącie unaczyniają : tętnica grzbietowa prącia i tętnica głęboka .
Z żył powierzchniowych krew odpływa do żyły grzbietowej prącia , z żył głębokich zaś do splotu sromowego .
Rozmiary penisa .
Istnieje szeroko rozpowszechnione przekonanie , że długość penisa u mężczyzny ma bezpośredni związek z jego sprawnością seksualną oraz satysfakcją partnerki podczas stosunku płciowego .
Seksuolodzy zaprzeczają temu i podają , że długość normalnego penisa w czasie spoczynku wynosi 6–10 cm , a podczas wzwodu ( erekcji ) 11–16 cm , podczas gdy głębokość pochwy to średnio 10 cm .
Notuje się również długość nieprzekraczającą 7 cm ( mikropenis ) , jak i przekraczającą 30 cm , aczkolwiek są to bardzo rzadkie przypadki .
Naczynia prącia .
Żyły głębokie prącia mają bardzo skomplikowaną budowę ; jest to ważne przy próbie podwiązania tych naczyń podczas operacji chirurgicznych .
