vilené we francuskim blazonie , są wtedy często kontrastowo ubarwione .
Niedźwiedź w herbie Appenzell jest przedstawiony z penisem , a pominięcie tej cechy było postrzegane jako poważna zniewaga .
W 1579 r. drukarz kalendarza w Saint Gallen zapomniał go dodać , co doprowadziło Appenzell na skraj wojny z Saint Gallen .
Do 2007 r. lew w herbie Nordic Battlegroup miał penisa , ale w 2007 r. dowódca zdecydował , że penis lwa musi zostać usunięty , w kontekście wykorzystywania seksualnego w strefach działań wojennych na świecie .
Jednak decyzja ta została zakwestionowana przez niektórych szwedzkich heraldyków , w tym artystę heraldycznego Vladimira Sagerlunda , który argumentował , że herby zawierające lwy bez penisa były historycznie nadawane tym , którzy zdradzili szwedzką koronę .
