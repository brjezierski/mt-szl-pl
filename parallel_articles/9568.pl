Párvusz
Párvusz ( czytaj `` Parwus '' , urodzony jako `` Norbert Kiss '' dnia 1 marca 1981 ) – węgierski artysta grafik .
Styl i prace .
Studiował w Budapeszcie i Segedynie .
Ogólnie styl Párvusza bazuje na atramencie , czasem używa on także akwareli , podstawowymi kolorami w jego pracach jest jednak czerń i biel .
Węgierski artysta Endre Szász i niderlandzki artysta M. C. Escher dość mocno wpłynęli na dzieła Párvusza i w 2004 odnalazł on swój własny styl , bazujący nieznacznie na pracach wcześniej wymienionych artystów .
W 2005 pojechał do Laponii , po powrocie na Węgry rozpoczął malowanie obrazków z motywami lapońskimi .
Najważniejsze dzieło z tego okresu to `` Lapońska Mona Lisa '' , sprzedana w Norwegii .
W 2007 pokazano w tamtejszej galerii w Tromsø siedem innych obrazów W 2009 Párvusz rozpoczął tworzenie obrazów wypalanych w drewnie .
