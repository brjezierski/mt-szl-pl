Pecz
Pecz ( [ ] , , , , , , ; dawne ) – miasto w południowych Węgrzech .
Piąte co do wielkości miasto Węgier ( 145 347 mieszkańców , powierzchnia 162,61 km² w I 2016 ) .
Leży na południowych zboczach gór Mecsek w Kraju Zadunajskim .
Siedziba władz komitatu Baranya .
Gospodarka .
Do niedawna ośrodek przemysłu górniczego ( kopalnie węgla kamiennego i uranu ) , obecnie ważny ośrodek akademicki i usługowy położony w okręgu rolniczym w pobliżu rejonu winiarskiego Villány .
Ponadto w mieście rozwinął się przemysł maszynowy , elektroniczny , chemiczny , włókienniczy , porcelanowy , meblarski oraz spożywczy .
Etymologia nazwy Pécs .
