Põltsamaa ( est .
`` Põltsamaa jõgi '' ) – rzeka w Estonii , najdłuższy dopływ Pedji , o długości 135 km .
Swoje źródła ma na terenie rezerwatu przyrody Endla , 5,5 km od miasta Tamsalu .
Płynie przez prowincje Jõgevamaa , Järvamaa i Virumaa Zachodnia .
Uchodzi do Pedji w pobliżu jeziora Võrtsjärv .
Ostatni odcinek od jej ujścia do ujścia Pedji do jeziora jest nazywany Pede .
Nad rzeką leży miasto Põltsamaa .
Ważniejszymi dopływami są : Ilmandu , Nõmme , Päinurme , Preedi , Oostriku .
