Pößneck
Pößneck – miasto w Niemczech położone we wschodniej części kraju związkowego Turyngia , w Saale-Orla-Kreis , największa miejscowość powiatu .
Geografia .
Pößneck położony jest w obniżeniu rzeki Orla , pomiędzy miastami Neustadt an der Orla na wschodzie i Saalfeld/Saale na zachodzie .
Historia .
Pößneck został po raz pierwszy wspomniany w dokumentach klasztoru z Saalfeld/Saale z 1252 r. Miejscowość została wtedy przekazana przez Fryderyka z rodu Wettynów , landgrafa Turyngii i margrabiego miśnieńskiego , hrabiom Schwarzburg jako lenno .
