Płaczki ( przystanek kolejowy )
Płaczki − wąskotorowy kolejowy przystanek osobowy w Płaczkach , w Polsce , w województwie wielkopolskim , w powiecie średzkim , w gminie Zaniemyśl .
Należy do Średzkiej Kolei Powiatowej .
Znajduje się na linii kolejowej ze Środy Wielkopolskiej Miasto do Zaniemyśla .
Linia ta została otwarta w dniu 19 sierpnia 1909 roku .
Pierwszy pociąg przejechał przez nią w dniu 1 maja 1910 roku .
Obecnie przez przystanek jeżdżą pociągi turystyczne ( od czerwca do końca sierpnia ) .
