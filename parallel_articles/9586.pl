W skład jednostki wszedł obszar dotychczasowej gromady Płaza ze zniesionej gminy Babice w tymże powiecie .
Dla gromady ustalono 26 członków gromadzkiej rady narodowej .
1 stycznia 1958 do gromady Płaza przyłączono obszar zniesionej gromady Pogorzyce .
31 grudnia 1961 do gromady Płaza przyłączono obszar zniesionej gromady Bolęcin .
Gromada przetrwała do końca 1972 roku , czyli do kolejnej reformy gminnej .
