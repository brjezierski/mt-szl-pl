Adwent
Adwent ( łac .
`` adventus '' – przyjście ) – w Kościołach chrześcijańskich okres trwający od I nieszporów czwartej z kolei poprzedzającej Święto Bożego Narodzenia niedzieli do zmierzchu 24 grudnia , okres przypominający oczekiwanie na powtórne przyjście Jezusa Chrystusa .
Jednocześnie jest to czas poprzedzający pamiątkę pierwszego przyjścia – wcielenia , znanego pod nazwą narodzin Chrystusa .
Początek tego okresu przypada pomiędzy 27 listopada a 3 grudnia .
Na adwent składają się cztery niedziele adwentowe , w pierwszą z nich rozpoczyna się w Kościołach chrześcijańskich rok liturgiczny .
Adwent to czas , w którym ewangelicy i katolicy mają uświadomić sobie , że oczekują na powtórne przyjście Jezusa Chrystusa .
W okresie adwentu dominuje w liturgii kolor fioletowy .
Kolorem tym w Kościołach ewangelickich nakrywa się ołtarz .
W Kościele katolickim kapłan zakłada do mszy świętej ornat w kolorze fioletowym , symbolizującym czas pokuty i przygotowania się do jednego z najważniejszych świąt , czasu pojednania z Bogiem i ludźmi , a w III niedzielę może zastąpić go kolorem różowym .
W niektórych Kościołach anglikańskich i starokatolickich , m.in .
w Reformowanym Kościele Katolickim w Polsce używa się niebieskiego koloru liturgicznego ( ang .
`` advent blue '' ) .
W Kościołach ewangelickich odprawiane są w tym czasie nabożeństwa adwentowe .
Tradycją w Kościołach katolickich są odprawiane w tym okresie o świcie msze , zwane roratami .
Są to msze ku czci Maryi Panny , na pamiątkę tego , że przyjęła nowinę od archanioła Gabriela , zwiastującego , iż zostanie Matką Syna Bożego .
