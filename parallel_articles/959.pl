Historia .
Początki adwentu należy łączyć z początkiem obchodzenia przez chrześcijan świąt Bożego Narodzenia około II połowy IV wieku .
Pierwsze ślady odkrywamy w liturgii hiszpańskiej i galicyjskiej .
