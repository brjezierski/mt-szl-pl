Pływanie
Pływanie – metoda poruszania się po powierzchni wody lub w wodzie , wykorzystywana przez człowieka , zwierzęta oraz maszyny .
Jest popularną aktywnością rekreacyjną , szczególnie w krajach o cieplejszym klimacie i naturalnych zbiornikach wodnych .
Istnieje wiele styli pływackich .
Do najpopularniejszych należą żabka , kraul , grzbietowy , motylkowy .
Wiele znanych i uznanych trenerów uważa , że do styli pływackich powinna się też zaliczać strzałka .
Pływanie to też jedna z metod rehabilitacji , a także dyscyplina sportu .
