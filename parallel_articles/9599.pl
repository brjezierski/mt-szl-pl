Pomorze ( jezioro )
Pomorze – jezioro położone w gminie Giby , powiat sejneński , województwo podlaskie , na południowy wschód od miasta Sejny .
Wpływa do niego , a później wypływa , rzeka Marycha .
Dawniej akwen ten nazywano morzem .
Nazwa ta była wynikiem slawizacji jaćwieskiego słowa `` mary '' , które oznaczało jezioro .
Charakterystyka .
Jezioro zaliczane jest do form rynnowo-wytopiskowych .
Ma mocno rozbudowaną linię brzegową .
Swoim kształtem przypomina trójkąt .
Jego powierzchnia wynosi : 297 ha , długość : 4,95 km , szerokość : 1,4 km , a maksymalna głębokość : 23,5 m. Brzegi jeziora w znacznej części porośnięte są lasem , przeważnie sosnowym , w większości łatwo dostępne .
Roślinność wynurzona nie jest zbyt bujna , składa się przede wszystkim z trzciny pospolitej i pałki wąskolistnej .
Bardziej urozmaicona jest roślinność zanurzona , tworząca miejscami podwodne łąki .
Dno jest głównie piaszczysto-żwirowe , przy brzegach bywa muliste .
Woda jest czysta , a jedynie podczas zakwitu fitoplanktonu jej przezroczystość znacznie spada .
