Cypr Północny
Cypr Północny ( tur .
Kuzey Kıbrıs ) , oficjalnie Turecka Republika Cypru Północnego ( tur .
Kuzey Kıbrıs Türk Cumhuriyeti ) – państwo częściowo uznawane na arenie międzynarodowej , zajmujące północną część azjatyckiej wyspy Cypr zamieszkiwaną przez ludność pochodzenia tureckiego .
Republika obejmuje ok. 25 % ludności i 36 % terytorium wyspy .
Ustrój polityczny .
Cypr Północny posiada własny rząd , parlament , konstytucję i hymn ( `` İstiklâl Marşi '' ) .
Cypr Północny administracyjnie został podzielony na 6 dystryktów , jednak z punktu widzenia Republiki Cypryjskiej jego tereny wciąż wchodzą w skład jednostek administracyjnych Cypru .
Uznanie międzynarodowe .
