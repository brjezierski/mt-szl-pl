Jabłonowo Pomorskie
Jabłonowo Pomorskie ( niem .
`` Jablonowo '' ) – miasto w województwie kujawsko-pomorskim , w powiecie brodnickim , siedziba gminy miejsko-wiejskiej Jabłonowo Pomorskie .
Położone na Pojezierzu Chełmińskim , nad rzeką Lutryną , przy drodze wojewódzkiej 543 .
W mieście krzyżują się linie kolejowe nr 208 i 353 .
Pod względem historycznym Jabłonowo Pomorskie leży w dawnej ziemi chełmińskiej .
Według danych GUS z 31 grudnia 2019 r. miasto liczyło 3716 mieszkańców .
Patronką miasta jest błogosławiona Maria Karłowska .
