Kowalewo Pomorskie ( stacja kolejowa )
Kowalewo Pomorskie – stacja kolejowa w Kowalewie Pomorskim , w województwie kujawsko-pomorskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
W roku 2017 stacja obsługiwała 150–199 pasażerów na dobę .
Przez stację przebiegają linie kolejowe nr 353 oraz 209 .
