Kamień Pomorski
Kamień Pomorski ( ) – miasto w województwie zachodniopomorskim , w powiecie kamieńskim , siedziba gminy miejsko-wiejskiej Kamień Pomorski .
Uzdrowisko położone na Pobrzeżu Szczecińskim , nad brzegiem Zalewu Kamieńskiego .
Druga siedziba archidiecezji szczecińsko-kamieńskiej i tutejszego dekanatu .
Zlokalizowany jest tu port morski z basenem rybacko-żeglarskim .
Kamień to pierwsza znana stolica Pomorza .
Według danych GUS z 30 czerwca 2021 r. , Kamień Pomorski liczył 8627 mieszkańców i był pod względem liczby ludności 26. miastem w województwie zachodniopomorskim .
Położenie .
Kamień Pomorski położony jest na Pobrzeżu Szczecińskim ( Wybrzeże Trzebiatowskie ) , w rozlewisku cieśniny Dziwny tworzącej Zalew Kamieński .
Miasto leży nad dwiema zatokami zalewu : Karpinką od północy i Promną od zachodu , za którą znajduje się Wyspa Chrząszczewska .
Miejscowość znajduje się 90 km na północny wschód od Szczecina .
Kamień Pomorski leży w północnej części Pomorza Zachodniego .
