Język adygejski
Język adygejski ( , trl .
`` adygebze '' ) – język z rodziny języków abchasko-adygejskich , jeden z oficjalnych języków ( obok rosyjskiego ) Republiki Adygei w Federacji Rosyjskiej .
Języka używa obecnie około 300 tys .
ludzi .
Poza Rosją język adygejski używany jest także m.in .
w Turcji , Jordanii , Syrii , Izraelu , Macedonii Północnej i Iraku .
Język wraz z ubyskim , abchaskim , kabardyjskim i abazyńskim tworzy grupę języków północno-zachodnio-kaukaskich ( abchasko-adygejskich ) .
Język adygejski został ustandaryzowany po rewolucji październikowej .
Od 1938 roku do pisowni używa się cyrylicy .
Wcześniej do zapisu języka adygejskiego używano alfabetu arabskiego i łacińskiego .
