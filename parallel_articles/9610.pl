Od zachodu Amerykę Północną otacza Ocean Spokojny , od północy Ocean Arktyczny , a od wschodu Ocean Atlantycki .
Ameryka Północna od Azji oddzielona jest Cieśniną Beringa .
Na południu łączy się z Ameryką Południową .
Południowa część nazywana jest Ameryką Łacińską .
