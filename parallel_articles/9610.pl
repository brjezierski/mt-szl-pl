W Ameryce Północnej zaznacza się przewaga gór i wyżyn nad nizinami – średnia wysokość nad poziomem morza to 781 m. Najwyższym punktem jest Denali ( znany też jako McKinley – 6194 m ) na Alasce , natomiast najniższy znajduje się w Dolinie Śmierci ( 86 m p.p.m . ) .
Ponad 64 % powierzchni leży powyżej 300 m n.p.m .
Wzdłuż zachodnich brzegów Kordyliery ( aktywne sejsmicznie ) z wewnętrznymi wyżynami ( Kolorado , Meksykańska ) , wewnątrz Wielkie Równiny i Niziny Wewnętrzne , na wschodzie Appalachy .
