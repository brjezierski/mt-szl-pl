Dakota Północna
Dakota Północna ( , ) – stan na północy Stanów Zjednoczonych , leżący w regionie Midwest , na obszarze Wielkich Równin .
Nazwa stanu pochodzi od słowa Indian Siuksów ( nazywanych także Dakotami ) oznaczającego „ przyjaciela ” .
Od zachodu graniczy z Montaną , od południa z Dakotą Południową , od wschodu z Minnesotą , a od północy z kanadyjską prowincją Manitoba .
Rzeźba terenu jest na ogół wyżynna .
Dakota Północna jest uważana za najbardziej wiejski stan USA , ponieważ około 90 % jej gruntów jest wykorzystywanych w celach rolniczych .
