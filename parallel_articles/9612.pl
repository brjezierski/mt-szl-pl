Karolina Północna
Karolina Północna ( ang .
`` North Carolina '' ) ( ) – stan w południowo-wschodniej części Stanów Zjednoczonych , graniczący od północy z Wirginią od południa z Georgią i Karoliną Południową , a od zachodu z Tennessee .
Granicę wschodnią stanowi Ocean Atlantycki .
Obszar metropolitalny Charlotte liczy 2,7 mln mieszkańców i jest największy w stanie .
Stolicą jest Raleigh z 1,45 mln mieszkańców w obszarze metropolitalnym .
Historia .
4 lipca 1584 na wybrzeżu dzisiejszej Karoliny Północnej wylądowała angielska ekspedycja pod wodzą Philipa Amadasa i Arthura Barlowa z zadaniem założenia kolonii .
W 1585 założono pierwszą kolonię na wyspie Roanoke , która jednak nie przetrwała ( tzw .
Zaginiona Kolonia ) .
Pierwsi stali angielscy osadnicy przybyli z sąsiedniej Wirginii na początku XVII wieku .
Od 1663 do 1712 istniała kolonia pod nazwą `` Province of Carolina '' , ustanowiona na podstawie przywilejów wydanych przez króla Anglii Karola II Stuarta i nazwana na cześć jego ojca Karola I Stuarta .
Około 1712 kolonia ta formalnie podzieliła się na Karolinę Północną i Karolinę Południową ( faktycznie te dwie części rozwijały się samodzielnie od początku europejskiego osadnictwa ) .
21 listopada 1789 kolonia ratyfikowała Konstytucję Stanów Zjednoczonych .
Podczas wojny secesyjnej Karolina Północna była jednym ze stanów Konfederacji .
17 grudnia 1903 na mierzei między miejscowościami Kitty Hawk a Kill Devil Hills miał miejsce pierwszy lot samolotu konstrukcji braci Wright .
Po wojnie secesyjnej Karolina Północna uprzemysłowiła swoje miasta setkami fabryk , aby stać się światową stolicą mebli i głównym producentem tekstyliów i wyrobów tytoniowych .
Z czasem nowe linie kolejowe pozwoliły przekształcić tytoń , bawełnę i drewno w dochodowe produkty .
