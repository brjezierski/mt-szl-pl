Korea Północna
Korea Północna , oficjalna nazwa Koreańska Republika Ludowo-Demokratyczna , w skrócie KRLD ( kor .
조선민주주의인민공화국 , hancha : 朝鮮民主主義人民共和國 , MCR `` Chosŏn Minjujuŭi Inmin Konghwaguk '' ) – państwo w Azji Wschodniej , powstałe w północnej części Półwyspu Koreańskiego po II wojnie światowej na terenach zajętych przez Armię Czerwoną .
Oficjalnie , według konstytucji , Koreańska Republika Ludowo-Demokratyczna jest republiką socjalistyczną .
W rzeczywistości jest jednak dyktaturą totalitarną opartą na rządach przywódców z dynastii Kimów .
Graniczy z Chińską Republiką Ludową , Federacją Rosyjską oraz Koreą Południową .
Geografia .
Korea Północna zajmuje północną część Półwyspu Koreańskiego o powierzchni 120 540 km² .
Od zachodniej strony jej granicę wyznacza Morze Zachodniokoreańskie ( znane lepiej jako Morze Żółte ) i Zatoka Zachodniokoreańska , od wschodu – Morze Wschodniokoreańskie ( Morze Japońskie ) .
Rzeki Amnok ( inaczej Yalu ; najdłuższa rzeka Korei , długość 790 km ) oraz Tuman-gang ( dł .
521 km ) wytyczają granicę KRLD z Chinami .
Najwyższy punkt Korei Północnej stanowi góra Pektu-san ( 2744 m n.p.m . ) .
Na północno-wschodnim krańcu państwa przebiega granica z Rosją .
Strefę buforową między obiema Koreami stanowi Koreańska Strefa Zdemilitaryzowana ( dł .
238 km ) .
Największym miastem Korei Północnej i stolicą tego państwa jest Pjongjang ( 3 255 388 mieszkańców , 2008 ) .
Inne duże miasta to Kaesŏng ( 308 tys. , 2008 ) tuż przy granicy z Koreą Południową , Sinŭiju ( 285 tys. , 2009 ) w północno-zachodniej części kraju , przy granicy z Chinami , Wŏnsan ( 329 tys. , 2005 ) i Hamhŭng ( 581 tys. , 2008 ) na wschodzie oraz Ch ’ ŏngjin ( 329 tys. , 2008 ) na północnym wschodzie .
Topografia .
Europejscy odkrywcy , którzy docierali niegdyś na Półwysep Koreański , określali ukształtowanie geograficzne terytorium Korei jako „ morze z huraganami ” .
Aż 80 % terenu Korei stanowią góry i wyżyny , przedzielone głębokimi i wąskimi dolinami .
Wszystkie szczyty Półwyspu Koreańskiego , osiągające 2000 m n.p.m. , znajdują się na terytorium Korei Północnej .
Nadmorskie równiny są szerokie na zachodzie i nieciągłe nad wschodnim wybrzeżem .
Zdecydowana większość ludności KRLD żyje właśnie na równinach i nielicznych terenach nizinnych .
