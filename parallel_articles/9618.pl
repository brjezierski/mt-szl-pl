P ’ yŏngsŏng ( stacja kolejowa )
P'yŏngsŏng ( kor .
평성역 , `` P'yŏngsŏng-yŏk '' ) – stacja kolejowa w centralnej części Korei Północnej , na 36. kilometrze 819-kilometrowej linii P'yŏngna , łączącej Pjongjang oraz specjalną strefę ekonomiczną Rasŏn .
Znajduje się w administracyjnych granicach miasta P'yŏngsŏng ( prowincja P'yŏngan Południowy ) .
Ze względu na fakt , iż obywatele Korei Północnej potrzebują pozwoleń na przemieszczanie się po kraju , stacja często jest zatłoczona – właśnie tutaj sprawdzane są pozwolenia na wjazd do Pjongjangu .
