Afganistan
Afganistan – śródlądowe państwo unitarne położone w Azji Południowej , ze stolicą w Kabulu .
Graniczy pod względem największych granic kolejno z : Pakistanem , Iranem , Tadżykistanem , Turkmenistanem , Uzbekistanem i Chinami .
Afganistan jest `` de facto '' kontrolowany przez talibów jako Islamski Emirat Afganistanu od 15 sierpnia 2021 , czyli od upadku Islamskiej Republiki Afganistanu , uznawanej na arenie międzynarodowej .
