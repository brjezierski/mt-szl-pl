Źródło słowa „ Afgan ” pozostaje nierozpoznane .
Jedno z wyjaśnień bierze je od Apakana , irańskiego władcy z VIII lub IX wieku .
Inne wskazuje na odniesienie do słowa „ Abgan ” ( Imperium Sasanidów , III wiek ) , najstarszego znanego zapisu odnoszącego się do późniejszego „ Afgan ” .
Pojawia się również w zapiskach Szapura I w Nachsz-e-Rostam , gdzie wspomina o nazwisku Goundifer Abgan Rismaund .
Indyjski astronom z VI wieku , Varahamihira , w swojej pracy „ Brhat Samhita ” ( 11.61 ; 16.38 ) , określa Afganów słowem `` Avagan . ''
Chiński pielgrzym Xuanzang określa nazwą 阿薄健 ( „ Abojian ” ) lud zamieszkujący tereny na północ od Gór Sulejmańskich .
W języku perskim `` -stân '' oznacza „ kraj ” .
Starożytni Grecy nazywali te tereny „ Ariana ” od awestyjskiego `` Aryanam Vaeja '' lub „ Aryavarta ” – „ kraj Ariów ” w sanskrycie ( por .
`` Iran '' ) .
Określenie ‘ Ariana Afghânistân ’ jest wciąż popularne wśród perskojęzycznych mieszkańców Afganistanu .
Nazwą 'Chorasan ' określano Afganistan jako część Wielkiego Chorasanu , co w zapisie pahlawi znaczy „ Wschodni Kraj ” ( pers .
