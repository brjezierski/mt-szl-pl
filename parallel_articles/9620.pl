Qélé , Qélé
Qélé , Qélé ( orm .
: Քելե Քելե ) – singel ormiańskiej piosenkarki Sirusho napisany przez wokalistkę we współpracy z H.A .
Der-Hovagimianem i wydany w 2008 roku .
8 marca 2008 roku piosenka została wybrana na utwór reprezentujący Armenię podczas 53 .
Konkursu Piosenki Eurowizji .
Zdobyła największe poparcie od komisji sędziowskiej oraz telewidzów .
Chórzystką na nagraniu studyjnym piosenki była Tigran Petrosjan .
Wokalistka wyruszyła w trasę koncertową po Europie .
20 maja odbył się pierwszy półfinał konkursu , w którym „ Qélé , Qélé ” zostało zaprezentowane jako czternaste w kolejności .
Zdobył 139 punktów , które zapewniły mu awans do finału z drugiego miejsca .
Podczas sobotniego finału cztery dni później utwór zdobył 199 punktów , co przełożyło się na 4. miejsce .
Dotychczas jest to najwyżej oceniona ormiańska propozycja eurowizyjna w historii konkursu .
