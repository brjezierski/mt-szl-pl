Jest jednak postacią kontrowersyjną , gdyż porządek w państwie utrzymywał , stosując despotyczne metody rządzenia , w tym terror .
Zjednoczenie i unifikacja przesądziły o losach Chin , które do dziś pozostają ( w zasadzie ) jednym państwem , choć rozmiarami dorównują kontynentowi europejskiemu i są bardzo zróżnicowane językowo oraz etnicznie .
Imię , nazwisko i tytulatura .
Przyszły pierwszy cesarz miał na imię Zheng ( chiń .
政 ) , dlatego przez historyków jest często określany jako król Zheng .
Nie jest to jednak poprawne połączenie , bo imię używane było wyłącznie przez członków najbliższej rodziny .
Podobnie niehistoryczny jest tytuł książę Zheng ( do czasu wstąpienia na tron ) .
Przed zjednoczeniem Chin władca był znany swoim poddanym po prostu jako Król Qinu .
Inną utartą , acz historycznie niepoprawną formą podawania imienia króla , jest łączenie go z nazwiskiem rodowym ( `` xing '' ) – Ying Zheng ( 贏政 ) .
W przeciwieństwie do czasów obecnych , w ówczesnych Chinach imienia i nazwiska nie używano łącznie .
Jego przydomek ( `` shi '' ) – Zhao ( chiń .
趙 ) , wywodził się od państwa Zhao , w którym przyszedł na świat .
Przez sobie współczesnych było określany jako Zhao Zheng ( co na język polski można przetłumaczyć również jako „ Zheng z Zhao ” ) .
Po zjednoczeniu Chin władca przyjął nowy , wymyślony przez siebie tytuł – huangdi ( 皇帝 ) .
