Quetzaltenango ( miasto )
Quetzaltenango – miasto w południowo-zachodniej Gwatemali , położone w Kordylierach na wysokości 2350 m n.p.m .
Ludność : 172 tys .
( 2013 ) – czwarte pod względem liczby mieszkańców miasto Gwatemali .
Ośrodek administracyjny departamentu Quetzaltenango .
Miasto leży na wysokości 2330 m n.p.m. , około 5 km na północ od wulkanu Almolonga .
Miasto zostało założone w XIV wieku przez Majów posługujących się językiem mam .
Nosiło wówczas nazwę `` Xelajú '' , co oznacza „ ( miasto ) pod dziesięcioma górami ” .
Do dzisiaj miasto często nazywane jest `` Xela '' .
