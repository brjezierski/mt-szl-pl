Quezon City
Quezón City ( fil .
`` Lungsod Quezon '' , hiszp .
`` Ciudad Quezon '' ) – historyczna stolica Filipin w latach 1948–1976 , wchodząca obecnie w skład Narodowego Terytorium Stołecznego Metro Manila .
Miasto położone jest na wyspie Luzon , na północny wschód od obecnej stolicy Manili .
W 2010 roku jego populacja liczyła 2 761 720 mieszkańców .
Historia .
Miasto zostało nazwane na cześć Manuela L. Quezona , prezydenta Wspólnoty Filipin w latach 1935–1944 , który był jego założycielem .
Już na początku XX wieku Quezón marzył o mieście , które mogłoby być nową stolicą kraju .
