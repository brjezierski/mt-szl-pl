Quimperlé
Quimperlé ( bret .
`` Kemperle '' ) – miejscowość i gmina we Francji , w regionie Bretania , w departamencie Finistère .
Nazwa miejscowości pochodzi od `` kemper '' , bretońskiego odpowiednika łac .
`` confluentes '' ( `` spływ , zlanie się rzek '' : `` ken '' - `` cum '' i `` bera '' - `` fluere '' ) , pojawiającego się w toponimii bretońskiej w formie `` kemper '' , gdzie inicjalne `` k '' obecnie jest zastąpione przez grupę `` qu '' .
Quimperlé to początkowo Kemper-Ellé ; znajduje się ono w miejscu , gdzie zbiegają się rzeki Isole i Ellé .
