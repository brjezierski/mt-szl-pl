Jest największą prowincją w Kanadzie pod względem powierzchni , a pod względem liczby ludności ustępuje jedynie Ontario .
Na zachodzie graniczy z Ontario , na północy przez Zatokę Hudsona z terytorium Nunavut , na wschodzie z kontynentalną częścią Nowej Fundlandii i Labradoru oraz z Nowym Brunszwikiem .
Od południa graniczy ze stanami USA : Nowy Jork , New Hampshire , Vermont i Maine .
