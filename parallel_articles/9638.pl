R.E.M .
( skrót od ang .
`` rapid eye movement '' – jedna z faz snu ) była grupą , której muzyka łączyła elementy punk rocka i rocka alternatywnego .
Grupa złagodziła punkowe brzmienie , wprowadzając jangle popową grę gitar i charakterystyczny śpiew Michaela Stipe ' a , który stał się wyróżnikiem grupy .
Pozostawiono jednak spontaniczność i żywiołowość muzyki punkowej .
Początki .
Przez pierwsze lata swej działalności grupa regularnie wydawała albumy i koncertowała .
Z czasem jej brzmienie nabrało odcieni melancholijnych - aranżacjom utworów wprowadzono sekcję smyczków , a teksty grupy wyrażały niepokój .
