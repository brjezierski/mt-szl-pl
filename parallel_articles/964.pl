Afric Simone
Afric Simone ( ur .
17 lipca 1956 w Inhambane ) – muzyk pochodzący z Mozambiku .
W swojej muzyce miesza rozmaite wątki , zwłaszcza przesłanie folkloru afrykańskiego z europejską muzyką pop .
Niektóre jego kompozycje uległy też wpływowi rytmów latynoamerykańskich i muzyki karaibskiej .
Jego utwory charakteryzują się prostotą .
Znaczenie języka .
Afric Simone używa w tekście swoich piosenek rozmaitych afrykańskich języków i dialektów , a o problemie „ zrozumiałości ” tekstów mówi :
Kariera muzyczna .
Syn Brazylijczyka i Mozambijki , urodzony w Inhambane ( Mozambik ) .
Po śmierci ojca , w wieku 9 lat przeprowadził się wraz z matką do Xipamamine , dzielnicy Lourenço Marqes ( od 1976 Maputo ) gdzie dorastał .
Tam też pierwszy raz zaprezentował się na estradzie .
Szlify dla swojego przyszłego zawodu zdobywał w Londynie , gdzie działał jako wokalista , a przede wszystkim jako tancerz rytmiczny .
Gdy przeprowadził się do kontynentalnej Europy , nagrał pierwszą swoją małą płytę ( `` Mr. Barracuda '' ) i dotarł wraz z nią na szczyt popularności w Wenezueli .
Zdobył uznanie także w Boliwii , Peru i Brazylii , a w Wenezueli i Kolumbii zdobył złotą płytę .
