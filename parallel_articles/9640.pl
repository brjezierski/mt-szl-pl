ROW 1964 Rybnik
ROW 1964 Rybnik – polski klub piłkarski powstały w 1964 roku w Rybniku i występujący w IV lidze .
Historia i opis klubu .
ROW Rybnik powstał jako klub wielosekcyjny w 1964 r. z połączenia kilku mniejszych klubów , rozwijając zwłaszcza takie dyscypliny jak piłka nożna ( m.in .
