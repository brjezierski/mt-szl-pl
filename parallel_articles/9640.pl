Piłkarski ROW powstał w 1964 roku z połączenia się dwóch klubów sportowych : Górnika Rybnik z Górnikiem Chwałowice ( dzielnica Rybnika ) .
W czasach świetności klubu liczył on 13 sekcji , najbardziej popularne : sekcja żużlowa ( ROW Rybnik ) i piłki nożnej .
Na początku lat 90. wraz z przemianami gospodarczymi w kraju , KS ROW Rybnik przeszedł w stan upadłości .
W 2003 roku na bazie występującego w klasie okręgowej klubu RKS Energetyk Rybnik , reaktywowano piłkarski ROW , który pod szyldem KS Energetyk ROW Rybnik stopniowo pnie się w górę rozgrywek ligowych .
W 2013 roku awansował na zaplecze ekstraklasy ( I liga ) .
Dzięki aktywności kibiców , klub w czerwcu 2015 powrócił do oryginalnej nazwy KS ROW 1964 Rybnik .
