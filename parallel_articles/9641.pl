Ra ’ ananna
Ra ’ ananna ( ) – miasto położone w Dystrykcie Centralnym w Izraelu .
Leży na równinie Szaron w aglomeracji miejskiej Gusz Dan , w otoczeniu miast Kefar Sawa i Herclijja , miasteczka Kefar Szemarjahu , moszawów Kefar Malal , Ramot ha-Szawim , Giwat Chen , Riszpon , Bene Cijjon , Bacra , Sede Warburg i Gan Chajjim , oraz kibucu Szefajim .
Historia .
W 1921 roku żydowska społeczność `` Ahuza Alef '' z Nowego Jorku ( USA ) zakupiła ziemię pod założenie osady rolniczej w Palestynie .
2 kwietnia 1922 pierwsza grupa czterech pionierów ze Stanów Zjednoczonych i Kanady wraz z trzema wynajętymi robotnikami i dwoma uzbrojonymi strażnikami , rozbiła na tej ziemi namiot .
Nową osadę nazwano Ra ’ ananna , jednak arabscy sąsiedzi nazwali ją `` Americaya '' , ponieważ jej mieszkańcy mówili po angielsku i pochodzili z Ameryki .
Ostatecznie osadnicy zdecydowali się na mówienie w języku hebrajskim .
W 1936 Ra ’ ananna otrzymała status samorządu lokalnego .
W 1948 w miasteczku żyło już 300 mieszkańców .
W Ra ’ ananna osiedliło się wielu imigrantów z Ameryki Północnej , Wielkiej Brytanii i Afryki Południowej , nadając miastu niepowtarzalny klimat .
W ostatnich latach osiedliło się tutaj także wielu imigrantów z Francji .
