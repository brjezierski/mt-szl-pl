Rabaul
Rabaul – miasto w Papui-Nowej Gwinei w północnej części wyspy Nowa Brytania na półwyspie Gazelle .
Ludność : 8900 ( 2000 ) .
Do roku 1994 ośrodek administracyjny prowincji Nowa Brytania Wschodnia .
Rozwinął się tutaj przemysł spożywczy , meblarski i materiałów budowlanych .
Przez port morski w Rabaul eksportuje się koprę , kakao i olej kokosowy .
Znajduje się tutaj drugi międzynarodowy port lotniczy kraju .
W roku 1994 wybuch wulkanu Tavurvur zniszczył port lotniczy .
Do erupcji z roku 1994 Rabaul był popularnym ośrodkiem turystyczno-wypoczynkowym , szczególnie ze względu na atrakcyjne miejsca do swobodnego nurkowania .
