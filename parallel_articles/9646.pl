Rabka-Zdrój ( przystanek kolejowy )
Rabka-Zdrój – przystanek kolejowy w Rabce-Zdroju , w województwie małopolskim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca turystycznego .
W roku 2021 przystanek obsługiwał 100–149 pasażerów na dobę .
Przystanek został otwarty w 1884 roku w ramach budowy Galicyjskiej Kolei Transwersalnej .
W 1993 zabudowa zespołu dworcowego została wpisana do rejestru zabytków .
W latach 1927–1939 oraz 1945–1952 przystanek ten figurował w spisach jako stacja kolejowa .
