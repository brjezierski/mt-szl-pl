Rachel Carson
Rachel Carson ( ur .
27 maja 1907 w Springdale , zm .
14 kwietnia 1964 w Silver Spring ) – amerykańska biolog , autorka książek popularnonaukowych .
Tytuł magistra zoologii uzyskała na Uniwersytecie Johnsa Hopkinsa .
Pierwszą pracę podjęła w Urzędzie ds .
Rybołówstwa , a jej zadaniem było opracowanie liczącego 52 odcinki programu radiowego „ Romance Under the Waters ” .
W 1937 r. opublikowała artykuł „ The Atlantic ” , w którym opowiadała o oceanie nie z perspektywy człowieka , a żyjących w nim zwierząt i roślin .
Artykuł doprowadził do publikacji jej pierwszej książki „ Under the Sea-Wind ” .
„ Morze wokół nas ” , które opublikowała w 1951 r. zdobyło prestiżową National Book Award w dziedzinie literatury popularnonaukowej .
Jej książka `` Silent Spring '' zwróciła uwagę społeczeństwa na sprawy ekologii i ochrony środowiska , a także znacząco przyczyniła się do powstania globalnego ruchu ekologicznego .
`` Silent Spring '' najpierw we fragmentach ukazało się w „ The New Yorker ” , publikacja książkowa o niszczycielskich skutkach nadużywania pestycydów miała miejsce w 1962 .
