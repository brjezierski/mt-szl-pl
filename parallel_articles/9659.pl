Radhošť
Radhošť , pol .
Radhoszcz ( 1129 m n.p.m . )
– szczyt górski w Beskidzie Śląsko-Morawskim , według podań miejsce kultu Radogosta .
Góra znajduje się 7 km od miasta Rožnov pod Radhoštěm , z którego prowadzi i kończy się na szczycie czerwony szlak .
Z kolei z zachodu na wschód w kierunku przełęczy Pustevny przez górę Radegast prowadzi szlak niebieski .
Na szczycie znajduje się krzyż z 1805 roku , kaplica pw .
śś .
Cyryla i Metodego wraz z towarzyszącymi rzeźbami obu świętych z lat 1896-1898 , oraz 46-metrowy maszt telewizyjny .
Ok. 300 m na południowy wschód od szczytu znajduje się hotel Radegast , a pomiędzy nim a szczytem placówka ratownictwa górskiego .
Na szczyt prowadzi również wyciąg bezpodporowy .
Niedaleko od wierzchołka znajduje się także wieża triangulacyjna .
Północne stoki góry objęte są od 1955 ochroną przyrody w postaci narodowego rezerwatu przyrody Radhošť .
