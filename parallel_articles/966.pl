Afroamerykanie
Afroamerykanie ( , również ) – czarnoskórzy Amerykanie pochodzenia subsaharyjskiego .
W głównej mierze są potomkami Afrykanów , sprowadzanych od XVII do XIX wieku jako niewolnicza siła robocza , niektórzy wywodzą się z późniejszej imigracji z krajów Afryki oraz Karaibów i Ameryki Łacińskiej ( gł .
z Jamajki i Haiti ) .
