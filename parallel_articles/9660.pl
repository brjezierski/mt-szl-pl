Radio Piekary
Radio Piekary – regionalna rozgłośnia radiowa .
Na antenie dominują utwory muzyki polskiej , zagranicznej , a także muzyki śląskiej .
Stacja propaguje również śląską kulturę .
Historia .
Pierwszy Radioklub w Piekarach założono już w 1926 roku .
Przez te wszystkie lata krótkofalowcy działali bardzo prężnie .
Z inicjatywy działających w Radioklubie : Gintera Kupki , Janusza Dodolskiego i Stanisława Prokopa , w 1994 roku uruchomiono eksperymentalne radio , nadające program w zakresie fal ultrakrótkich .
Dzięki poparciu Zarządu Miasta i Rady Miejskiej wystąpiono o przyznanie koncesji dla `` Radia Piekary '' .
W wyniku starań formalno-prawnych i organizacyjnych natychmiast po otrzymaniu koncesji ( 16 października 1996 ) , w dniu 17 października 1996 roku o godz .
15.15 radio rozpoczęło nadawanie regularnych programów .
W 2009 roku rozgłośnia została laureatem Nagrody im .
Wojciecha Korfantego .
Informacje .
Według raportu Radio Track MillwardBrown SMG/KRC za okres grudzień – luty 2010/2011 przygotowanego dla portalu Wirtualnemedia.pl , Radio Piekary było drugie w rankingu najpopularniejszych stacji radiowych w aglomeracji górnośląskiej , z wynikiem 10,04 % udziału w rynku .
