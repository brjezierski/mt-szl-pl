Rzodkiew zwyczajna
Rzodkiew zwyczajna ( `` Raphanus sativus '' ) – gatunek roślin obejmujący trzy odmiany i wiele kultywarów .
Obecnie występuje tylko w uprawie i jest uprawiany w wielu regionach świata .
W Polsce występuje głównie jako roślina uprawna , lecz przejściowo dziczeje ( ergazjofigofit ) .
Biologia i ekologia .
Roślina jednoroczna lub dwuletnia .
Słupek i pręciki dojrzewają równocześnie , roślina owadopylna , kwitnie od maja do czerwca .
Roślina miododajna i dobre źródło pyłku dla pszczół .
Odmiany .
Występuje w 3 odmianach :
Zastosowanie .
Roślina uprawna : różne odmiany rzodkwi uprawiane są głównie jako warzywa , ale bywa uprawiana również jako roślina oleista i pastewna .
