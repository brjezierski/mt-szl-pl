Radlin II
Radlin II ( `` Radlin Drugi '' ) – dzielnica Wodzisławia Śląskiego .
Dzielnica o charakterze podmiejskim , historyczna część Radlina – Radlin właściwy .
Po zmianach administracyjnych pozostała jako część Wodzisławia Śl .
Przez Radlin II przebiega fragment drogi krajowej nr 78 oraz linii kolejowej nr 158 z przystankiem Wodzisław Śląski Radlin .
W dzielnicy ma swój początek rzeka Leśnica .
W dzielnicy od 1907 roku działa jednostka „ OSP Radlin 2 ” , znajdują się tu również dwa kościoły rzymskokatolickie pw .
Św .
Marii Magdaleny ( Dolniok ) i Św .
Izydora ( Górniok ) .
W skład dzielnicy wchodzą dawne miejscowości : Radlin Górny , Radlin Dolny , Szarowiec połączone w XIX wieku w wieś Radlin .
Dzielnica liczy około 5500 mieszkańców .
