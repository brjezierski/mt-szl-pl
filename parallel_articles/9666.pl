Radomiak Radom ( zapasy )
Radomiak Radom – sekcja zapasów powołana została w 1948 roku z inicjatywy trenera Władysława Miazia ( nota bene jako zapaśnicza i podnoszenia ciężarów ) .
Początkowo trenowało w niej 38 zawodników , wśród których wielu stanowiło reprezentację Radomiaka w rozgrywkach okręgu warszawskiego .
W 1949 roku zarząd Polskiego Związku Atletycznego powierzył szkoleniowcowi zorganizowanie w Radomiu mistrzostw Polski w zapasach i podnoszenia ciężarów ( będące jednocześnie eliminacjami do mistrzostw Europy w Pradze ) .
Andrzej Rejmont i Alfred Planeta uczestniczyli w obozie kondycyjnym dla reprezentacji Polski przed meczem z Czechosłowacją .
W latach 50. i 60 .
XX wieku zapaśnicy Radomiaka występowali w II lidze ( zaplecze ekstraklasy ) .
W latach 1950-1954 zawodnikiem radomskiej drużyny był m.in .
Jan Żurawski .
W 1966 roku szkoleniowca Miazię zastąpił Michał Kosna .
Za jego czasów brązowy medal mistrzostw Polski wywalczyło 2 zawodników : Stanisław Krzesiński i Andrzej Magiera .
W 1977 roku zapaśnicy Radomiaka pokonali reprezentację Stanów Zjednoczonych w hali RZO `` Radoskór '' .
W latach 80. i 90. trenerem sekcji był Jerzy Korczak .
W grudniu 1995 roku sekcja zawiesiła działalność .
Na jej bazie powstało Radomskie Towarzystwo Zapaśnicze .
Główną bazą treningową była Hala RZO „ Radoskór ” .
