Afroeurazja
Afroeurazja ( ang .
`` Eurafrasia '' , `` Afrasia '' , `` Afro-Eurasia '' ) – według niektórych koncepcji geograficznych jedna z części świata zajmująca ok. 74 816 000 km² , czyli 14,7 % powierzchni Ziemi .
Afroeurazja składa się z dwóch kontynentów : Eurazji ( rozróżnianej na Europę i Azję ) oraz Afryki .
Istnienie Afroeurazji nie jest uwzględniane przez zdecydowaną większość geografów ( np .
w Polsce nie jest uwzględniana w żadnym podręczniku akademickim geografii czy w encyklopediach ) głównie z powodu istnienia wyraźnej , geologiczno-tektonicznej , granicy pomiędzy Afryką i Eurazją .
Zwolennicy posiłkują się faktem , że przed powstaniem sztucznego Kanału Sueskiego można było przejść między Azją i Afryką .
Termin zbieżny ze starszym określeniem `` Stary Świat '' .
Ludność .
Afroeurazja jest zamieszkana przez 5 mld 988 mln ludzi , co stanowi 85,25 % ludności świata .
Najbardziej zaludnioną częścią Afroeurazji jest Azja , którą zamieszkuje 60,25 % populacji światowej , następnie Afryka ( 15 % ) i Europa ( 10,4 % ) .
Jeszcze w średniowieczu Afroeurazję zamieszkiwało 95 % ludności świata .
