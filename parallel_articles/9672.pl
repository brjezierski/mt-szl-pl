Radunia Stężyca
Radunia Stężyca – polski klub piłkarski z siedzibą w Stężycy , od sezonu 2021/2022 występujący w II lidze .
Historia .
Klub powstał w 1982 roku .
W sezonie 2020/2021 Radunia po raz pierwszy w swojej historii awansowała do II ligi .
W swoim pierwszym sezonie na tym szczeblu , Radunia zajęła 6. miejsce i zakwalifikowała się do baraży o pierwszą ligę , jednak uległa w półfinałowym starciu z Ruchem Chorzów 0:1 .
Zespół rozgrywa swoje domowe mecze na oddanym do użytku w 2017 stadionie wielofunkcyjnym „ Arena Radunia ” o pojemności 450 miejsc .
Klub ponadto prowadzi drużynę rezerw , występującą w sezonie 2022/2023 w klasy okręgowej , gr .
