Radymno
Radymno ( ; , `` Redem '' ) – miasto w Polsce , w województwie podkarpackim , w powiecie jarosławskim , siedziba gminy wiejskiej Radymno .
Dawna rezydencja biskupów przemyskich .
Prywatne miasto duchowne położone było na przełomie XVI i XVII wieku w ziemi przemyskiej województwa ruskiego Wchodziło w skład klucza radymnieńskiego biskupów przemyskich .
Od 23 grudnia 1920 do 18 sierpnia 1945 znajdowało się na terenie województwa lwowskiego .
W latach 1945–1975 wchodziło w skład województwa rzeszowskiego .
