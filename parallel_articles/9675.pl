Radziwiłłów ( stacja kolejowa )
Radziwiłłów ( ukr .
Радивилів , ros .
Радивилов ) – stacja kolejowa w miejscowości Radziwiłłów , w rejonie radziwiłłowskim , w obwodzie rówieńskim , na Ukrainie .
Stacja powstała w czasach Imperium Rosyjskiego na linii Kolei Południowo-Zachodniej .
Była wówczas rosyjską stacją graniczną na granicy z Austro-Węgrami .
Stacją graniczną po stronie austro-węgierskiej były Brody .
Pomiędzy obiema stacjami istniało kolejowe połączenie transgraniczne .
Po I wojnie światowej stacja straciła swój nadgraniczny charakter .
