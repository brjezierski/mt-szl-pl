Małpokształtne
Małpokształtne , małpy właściwe ( Simiiformes ) – infrarząd ssaków naczelnych z podrzędu wyższych naczelnych obejmujący małpy szerokonose i małpy wąskonose .
Występowanie .
Małpy szerokonose występują w obydwu Amerykach i nazywane są małpami Nowego Świata .
Współcześnie żyjące małpy wąskonose znane są z Azji i Afryki , dawniej występowały również w Europie ( obecnie tylko magot żyje na Gibraltarze ) – nazywane są małpami Starego Świata .
Należący do tego taksonu rodzaj człowiek `` Homo '' rozpowszechnił się na całym świecie , jedyny współczesny gatunek – człowiek rozumny `` H. sapiens '' występuje na wszystkich kontynentach i stworzył wysoko rozwiniętą cywilizację , umożliwiającą przebywanie osobnikom tego gatunku także poza Ziemią .
Charakterystyka małp .
Budowa zewnętrzna .
Należą tu zwierzęta bardzo rozmaitej wielkości , od pigmejki karłowatej ( `` Cebuella '' ( `` Callithrix '' ) `` pygmaea '' ) , mniejszej od wiewiórki pospolitej , do goryla , większego od człowieka .
Gatunki żyjące głównie na drzewach mają ciała wysmukłe i zgrabne , inne , naziemne lub naskalne , są krępe i masywne .
Owłosienie .
Ciało małp jest pokryte mniej lub więcej gęstym włosem , tworzącym często na głowie i karku rodzaj grzywy .
Pozbawione owłosienia mogą być jedynie twarz , dłonie , podeszwy nóg oraz nagnioty pośladkowe .
U człowieka owłosienie zredukowane na większości ciała .
Ubarwienie małp jest zwykle ciemne i skromne , jedynie nieowłosione części ciała bywają zabarwione jaskrawo .
Z rzadka też występują u niektórych rodzajów małp nieco żywsze i bardziej kontrastowe kolory owłosienia .
Czaszka .
W przeciwieństwie do innych rzędów ssaków , u małp część czaszki obejmująca mózg ( mózgoczaszka ) jest silnie rozwinięta i bardziej zaokrąglona .
Cechą charakterystyczną czaszek małp jest również kształt oczodołów , skierowanych ku przodowi ( wyraźniej niż u małpiatek ) i całkowicie oddzielonych od jam skroniowych .
Uzębienie małp jest pełne .
Małpy Starego Świata , w tym człowiek , mają 32 zęby : 8 dłutowatych siekaczy , 4 kły , 8 zębów przedtrzonowych i 12 zębów trzonowych .
Małpy Nowego Świata z rodziny pazurkowców ( Callithrichidae ) mają również 32 zęby , a pozostałe małpy szerokonose , należące do rodziny płaksowatych ( Cebidae ) mają 36 zębów : 8 siekaczy , 4 kły , 8 przedtrzonowych i 16 trzonowych zębów .
U samców niektórych gatunków występują bardzo silne i duże kły , które nadają czaszce tych małp znaczne podobieństwo do czaszki wielkich drapieżnych kotów .
Na szczęce dolnej u starych samców małp wąskonosych rozwija się silny grzebień kostny , służący do przyczepienia się potężnych mięśni żujących ; nadaje on charakter zwierzęcy całej czaszce .
Kończyny i ogon .
Kończyny przednie są zwykle dłuższe od tylnych .
Palce u wszystkich małp z wyjątkiem pazurkowcowatych są zaopatrzone w paznokcie ; u pazurkowcowatych obserwuje się pazury , które jednak na podstawie badań embriologicznych uznano za przekształcone paznokcie .
U wielu gatunków małp kciuki są słabo rozwinięte , a często nawet szczątkowe .
Wielki palec kończyn tylnych ( paluch ) jest dobrze rozwinięty i ułożony przeciwstawnie do pozostałych , co zapewnia kończynom tylnym chwytność .
U gatunków małp o długim ogonie , ogon spełnia często rolę chwytną .
Dolna jego powierzchnia jest wtedy naga i bogato unerwiona .
U wielu gatunków małp ogon jest krótki , a nawet szczątkowy .
Sposób poruszania .
Mięśnie kończyn są silne i pozwalają małpom wisieć dłuższy czas na jednej ręce i podciągać się na niej do góry .
Po drzewach małpy poruszają się zręcznie , niektóre przeskakują na gałęzie oddalone o więcej niż 10 m. Po ziemi większość małp chodzi na czworakach , opierając się na całej stopie , tylko małpy człekokształtne chodzą na dwóch nogach , opierając się na zewnętrznej krawędzi stopy .
Tylko gibbonowate chodzą na tylnych nogach z balansująco rozstawionymi na boki rękami .
Małpy człekokształtne podpierają się zwykle paliczkami środkowymi I , II i III palca dłoni .
Budowa i umięśnienie .
Mózg stosunkowo duży , o potężnie rozwiniętych półkulach mózgowych , przykrywających móżdżek .
Mózg ludzki wyróżnia się od innych małp dobrze rozwiniętą korą mózgową , szczególnie płatami skroniowymi .
Z narządów zmysłów najlepiej rozwinięte są oczy , obdarzone wysoką zdolnością akomodacyjną .
Dobrze jest również rozwinięty zmysł słuchu ; węch u małp właściwych odgrywa stosunkowo niewielką rolę .
U niektórych małp właściwych występują torby policzkowe i worek krtaniowy .
Charakterystyczny dla małp jest znaczny rozwój umięśnienia twarzy , dzięki czemu odznaczają się one bogatą mimiką .
W odróżnieniu od małpiatek małpy mają górną wargę suchą i niepodzieloną .
Uszy małp są nieruchome .
Na policzkach i pośladkach niektórych gatunków małp właściwych występują modzele , czyli obszary nieowłosionej skóry , pokrytej brodawkami i silnie zrogowaciałej .
Modzele te występują najczęściej na pośladkach u małp Starego Świata z wyjątkiem małp człekokształtnych .
Modzele mają często jaskrawe barwy .
Tryb życia .
Większość małp żyje w stadach lub grupach rodzinnych o rozbudowanej strukturze społecznej .
Występują też gatunki monogamiczne .
Często stadu przewodzi najmocniejszy samiec .
U samic występuje 4-tygodniowy cykl menstruacyjny , brak sezonowości urodzeń .
Samica rodzi zwykle jedno młode , które przez dłuższy czas jest przy matce , trzymając się mocno kończynami .
