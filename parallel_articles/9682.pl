Radomsko ( stacja kolejowa )
Radomsko – stacja kolejowa w Radomsku , w województwie łódzkim , w Polsce .
Według klasyfikacji PKP ma kategorię dworca regionalnego .
W roku 2018 stacja obsługiwała ok. 1400 pasażerów na dobę .
W 1846 roku wybudowano dworzec kolei warszawsko-wiedeńskiej .
27 października 1935 roku uroczyście otwarto nowy budynek dworca kolejowego wzniesiony według projektu inż .
