Radziejów ( Rybnik )
Radziejów – dzielnica Rybnika położona w południowej części miasta .
Liczba mieszkańców w 2019 r. wynosiła 1860 .
W dzielnicy znajduje się m.in .
200-letni dąb na popielowsko-radziejowskim cmentarzu .
Sport .
4 lipca 1920 roku w Radziejowie utworzone zostało gniazdo najstarszej polskiej organizacji sportowej Polskiego Towarzystwa Gimnastycznego „ Sokół ” .
Koło to podlegało organizacyjnie Towarzystwu Gimnastycznemu „ Sokół ” w Rybniku i należało do szeregu sekcji gimnastycznych śląskiego Sokoła .
W roku 1920 radziejowskie koło tej organizacji liczyło 50 członków .
Rozwój działalności Sokoła przerwał w 1939 roku wybuch II wojny światowej .
W okresie powojennym nie została reaktywowana ponieważ członkowie oraz sama idea towarzystwa była prześladowana przez władze komunistyczne .
