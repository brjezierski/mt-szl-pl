Rafael Correa
Rafael Vicente Correa Delgado ( ur .
6 kwietnia 1963 w Guayaquil ) − ekwadorski ekonomista i polityk , minister finansów od kwietnia do sierpnia 2005 , prezydent Ekwadoru od 15 stycznia 2007 do 24 maja 2017 .
Edukacja .
Rafael Correa w 1987 ukończył ekonomię na Universidad Catolica Santiago de Guayaquil .
Następnie przez rok pracował w centrum pomocy społecznej salezjanów w prowincji Cotopaxi .
Correa kształcił się również za granicą .
W 1991 ukończył nauki ekonomiczne na `` Universite Catholique de Louvain '' w Belgii .
W 1999 ukończył ekonomię na Uniwersytecie w Illinois , a w 2001 zdobył tytuł doktora ekonomii .
