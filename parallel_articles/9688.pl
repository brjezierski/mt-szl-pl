Rafał Trzaskowski
Rafał Kazimierz Trzaskowski ( ur .
17 stycznia 1972 w Warszawie ) – polski polityk i politolog , specjalista w zakresie spraw europejskich , doktor nauk humanistycznych , wykładowca akademicki .
Poseł do Parlamentu Europejskiego VII kadencji ( 2009–2013 ) , w latach 2013–2014 minister administracji i cyfryzacji , w latach 2014–2015 sekretarz stanu w Ministerstwie Spraw Zagranicznych , poseł na Sejm VIII kadencji ( 2015–2018 ) , od 2018 prezydent miasta stołecznego Warszawy , od 2020 wiceprzewodniczący Platformy Obywatelskiej .
Kandydat na urząd prezydenta RP w drugich wyborach w 2020 .
Życiorys .
Wykształcenie .
Przed wyborami w 1989 , będąc uczniem XI Liceum Ogólnokształcącego im .
Mikołaja Reja w Warszawie , zgłosił się jako wolontariusz do pomocy w biurze stołecznego Komitetu Obywatelskiego w kawiarni „ Niespodzianka ” przy placu Konstytucji 6 .
Pracował jako tłumacz języka angielskiego i wspierał m.in .
wizytującego Polskę przedstawiciela amerykańskiego stanu Michigan , dzięki któremu uzyskał później kilkumiesięczne stypendium , po czym kształcił się w Cranbrook-Kingswood High School w Bloomfield Hills ( 1990–1991 ) .
Maturę zdał w 1991 .
