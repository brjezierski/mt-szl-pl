Rafał Urban
Rafał Urban ( ur .
24 lipca 1893 w Winiarach , zm .
12 stycznia 1972 w Warszawie ) – polski pisarz , gawędziarz śląski , autor dramatu `` Termin Nyski , czyli sterylizacja naiwnego baranka nieludzka komedia autochtoniczna z epoki Trzeciego Rajchu w 5 aktach biurokratycznych … napisał chłop bezimienny , syn ziemi odzyskanej '' .
Życiorys .
Rafał Urban urodził się w rodzinie chłopskiej w Winiarach pod Głogówkiem ( Śląsk Opolski ) .
Naukę rozpoczął w miejscowej szkole ludowej .
Następnie trafił do gimnazjum w Nysie , skąd został wkrótce karnie relegowany za posiadanie , zakazanych wówczas , polskich książek i komunistycznych broszur .
Przez jakiś czas podejmował różne prace zarobkowe .
Szczęśliwym trafem zetknął się z księdzem Aleksandrem Skowrońskim – działaczem narodowym i poetą górnośląskim , dzięki któremu znów powrócił do gimnazjum , tym razem w Prudniku , tam też uzyskał maturę w roku 1914 .
Miał wówczas 21 lat .
Po wybuchu I wojny światowej został powołany do pruskiej armii .
Pod koniec walk , trafił do niewoli kanadyjskiej .
Po zwolnieniu , przez następne trzy lata , pracował jako ogrodnik w Anglii .
Potem powrócił na Śląsk , by po plebiscycie znów wyruszyć w świat .
Studiował dorywczo na kilku uniwersytetach m.in .
na Sorbonie .
Ukończył akademię Rolniczo-Leśną w Monachium , gdzie nauczył się zawodu rolnika i ogrodnika .
Po studiach pracował w instytucie hodowli roślin w Munchenburgu pod Berlinem .
W tym czasie zwiedził również Francję , Anglię , Włochy , Jugosławię , Holandię , kraje skandynawskie , Szwajcarię , Rosję i sporo krajów pozaeuropejskich .
W 1931 roku osiedlił się na stałe w swoich rodzinnych stronach w Winiarach .
Ożenił się z gospodarską córką z sąsiedniej wioski Anastazją Larysz .
W Winiarach przeżył niemal całą drugą wojnę światową i dopiero pod jej koniec został , wraz z całą rodziną , przymusowo ewakuowany w okolice Jeziora Bodeńskiego .
Powrócił w 1946 roku i zajął się rolnictwem oraz sadownictwem .
Jako pisarz Rafał Urban zadebiutował dopiero w 1948 roku .
Wziął udział w konkursie , ogłoszonym przez Towarzystwo Przyjaciół Nysy na utwór literacki , bądź naukowy , związany z ziemią nyską .
W dwóch kolejnych konkursach otrzymał wyróżnienie oraz przyznano mu dwukrotnie po 30 tys .
zł , za nadesłane utwory .
Jednym z nich była autobiograficzna opowieść pt .
`` Szkoła '' , drugim komediodramat `` Termin nyski '' – najbardziej bodaj znany utwór pisarza .
Oryginalny i nowatorski `` Termin nyski '' , wywołał spore kontrowersje i zakłopotanie wśród członków jury , jednakże ostatecznie uhonorowano Urbana nagrodą ( głównie głosami zasiadających w komisji profesorów : S. Kolbuszewskiego i Z. Bednorza ) , która jednak nie usatysfakcjonowała autora , gdyż była niewystarczająca na zakup , potrzebnego w gospodarstwie , konia .
Od momentu przyznania Urbanowi nagrody , zaczęła się jego pisarska kariera .
Twórczość pisarza rozsiana jest po różnych czasopismach , drukował m.in .
w katowickiej i opolskiej „ Odrze ” , „ Poglądach ” , „ Panoramie ” , „ Miesięczniku literackim ” , „ Przemianach ” , „ Głosach znad Odry ” , „ Życiu literackim ” , „ Opolu ” , „ Polityce ” , „ Trybunie Opolskiej ” , kalendarzach , almanachach i wydawnictwach zbiorowych .
Za życia Urban nie doczekał się jednak książkowego wydania swoich dzieł .
Od 1955 roku Rafał Urban należał do Związku Literatów Polskich i był pierwszym prezesem jego opolskiego oddziału .
12 stycznia 1972 roku Rafał Urban zmarł po operacji na oddziale onkologicznym w warszawskim szpitalu .
17 stycznia pochowano go przy kaplicy cmentarnej w Głogówku .
W 1978 roku odsłonięto w rodzinnym mieście pisarza jego , wykonane przez Adolfa Panitza , popiersie , a w setną rocznicę urodzin Rafała Urbana jedną z ulic Głogówka nazwano jego imieniem .
Twórczość .
Twórczość Rafała Urbana jest bardzo różnorodna , znajdują się w niej bajki , gawędy , opowiadania , wspomnienia , utwory dramatyczne , wiersze , artykuły publicystyczne na różne tematy , a także artykuły naukowe na temat sadownictwa i rolnictwa .
Utwory te , przez długi czas nie zostały zebrane w wydaniu książkowym i funkcjonowały w rozsypce na łamach lokalnych , przeważnie , czasopism .
Dopiero w 1982 roku , staraniem pracowników Instytutu Śląskiego w Opolu , ukazały się `` Pisma '' Urbana .
Książka zawiera obszerny wybór gawęd , kilka utworów lirycznych oraz dramatycznych , w tym debiutancki `` Termin nyski '' .
Po ośmiu latach , wyszedł tom drugi `` Pism '' , gdzie obok literatury pięknej znalazły się również artykuły pisarza , dotyczące sadownictwa i ogrodnictwa .
Centralnym tematem twórczości Urbana jest Śląsk w ujęciu realistycznym i historycznym , jak również baśniowym i mitycznym .
Ślązakami są , w większości , bohaterowie Urbana , śląskość obecna jest w obyczaju , folklorze , a przede wszystkim w języku .
Śląsk jest również uniwersalnym modelem i punktem wyjścia dla rozważań ogólnych tj .
kwestie etyczne , sztuka , refleksje na temat cywilizacji itp .
Twórczość o Rafale Urbanie .
Edward Stachura napisał piosenkę poświęconą swojemu przyjacielowi – Rafałowi Urbanowi , pt .
„ Piosenka dla Rafała Urbana ” .
Później została nagrana pod tym samym tytułem przez Stare Dobre Małżeństwo oraz przez Marka Gałązkę .
W miejscowości Brożec k. Krapkowic znajduje się ulica imienia Rafała Urbana .
Jedna z ulic w Katowicach , w dzielnicy Kostuchna została nazwana jego imieniem .
