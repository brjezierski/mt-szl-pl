Ag Apolloni
Ag Apolloni ( ur .
13 czerwca 1982 w Kačaniku ) - kosowski pisarz , poeta , filozof i filolog .
Jego dzieła wyróżniają się krytycznym spojrzeniem na historię , politykę i społeczeństwo .
Życiorys .
Ukończył w Kačaniku naukę podstawową i ponadpodstawową .
W 2005 roku ukończył studia na Wydziale Dramturgii i Literatury Uniwersytecie w Prisztinie .
Z przyczyn finansowych w 2006r .
zrezygnował ze studiów filozoficznych .
Spędził ponad 2 lata opiekując się swoim schorowanym ojcem , który zmarł w roku 2007 .
Rok później otrzymał tytuł magistra nauk filozoficznych , a w 2012 został magistrem nauk filologicznych .
Od 2008 roku pracuje jako profesor na Uniwersytecie w Prisztinie .
Praca .
Apolloni pracował jako dziennikarz , redaktor oraz redaktor naczelny w prisztińskich dziennikach i magazynach literackich i kulturalnych .
W 2010 przywrócił do życia najstarszy literacki magazyn `` Jeta e Re '' ( pol .
`` Nowe Życie '' ) , który prowadził przez następne trzy lata .
W 2013 założył czasopismo `` Symbol '' , w którym przeprowadził wywiady z wieloma działaczami kultury .
Jego prace zostały przetłumaczone na kilka języków , m.in .
angielski , niemiecki , włoski , czeski , macedoński , serbski .
Został nagrodzony międzynarodowymi nagrodami za swoje prace naukowe .
