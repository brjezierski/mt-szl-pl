Rakovník ( stacja kolejowa )
Rakovník – stacja kolejowa w Rakovníku , w kraju środkowoczeskim , w Czechach .
Jest ważnym węzłem kolejowym o znaczeniu regionalnym .
Znajduje się na wysokości 325 m n.p.m .
Jest zarządzana przez Správę železnic .
Na stacji znajdują się kasy biletowe , na których istnieje możliwość zakupu biletów na wszystkie pociągi w tym międzynarodowe oraz rezerwacji miejsc .
