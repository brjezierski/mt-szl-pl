Rakowiec ( obwód wołyński )
Rakowiec – wieś na Ukrainie , w obwodzie wołyńskim , w rejonie lubomelskim .
W II Rzeczypospolitej miejscowość była położona w gminie wiejskiej Bereźce w powiecie lubomelskim województwa wołyńskiego .
Miejscowość położona nad rzeką Neretwą ( ukr .
Нере́тва ) .
Wieś zamieszkiwało ponad 65 polskich rodzin .
Region objęty szczególnym prześladowaniem ludności polskiej w czasie wydarzeń rzezi wołyńskiej , o której są lokalne publikacje ( niedokończone Jana Andrzeja Klekota oraz Józefa Turowskiego , „ Pożoga '' zmarłego w 1944 roku ) .
Obecnie jest to obszar wsi Pryriczczia .
