Koralówka zwiędła
Koralówka zwiędła ( `` Ramaria flaccida '' ( Fr . )
Bourd . )
– gatunek grzybów z rodziny siatkoblaszkowatych ( `` Gomphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Gomphaceae , Gomphales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' ( według Index Fungorum ) .
Gatunek pierwotnie opisany został jako `` Clavaria flaccida '' przez Eliasa Friesa w 1821 r. Do rodzaju `` Ramaria '' przeniesiony został przez Huberta Bourdota w 1898 r. W nowszej rewizji systematycznej tego rodzaju proponowany jest do przeniesienia pod nazwę `` Phaeoclavulina flaccida '' ( Fr . )
Giachini ( 2011 ) .
Synonimów naukowych ma 11 .
