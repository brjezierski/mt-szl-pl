Miasto jest ośrodkiem przemysłu rybnego i cementowego .
Międzynarodowy port lotniczy , 10 kilometrów piaszczystych plaż oraz średnio 300 słonecznych dni w roku sprawia , że Agadir jest najpopularniejszą nadmorską miejscowością wypoczynkową w Maroku .
Historia miasta .
Pierwszą osadą , która znajdowała się nad Atlantykiem u ujścia rzeki Wadi Sus , była wspomniana przez Herodota fenicka kolonia Thymiaterium .
Współczesne miasto założyli w XV wieku Portugalczycy .
W kolejnym stuleciu Agadir został zajęty przez wojska marokańskiej dynastii Sadytów .
