Koralówka żółknąca
Koralówka żółknąca ( `` Ramaria flavescens '' ( Schaeff . )
R.H. Petersen ) – gatunek grzybów z rodziny siatkoblaszkowatych ( `` Gomphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramaria , Gomphaceae , Gomphales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1774 r. Jacob Christian Schaeffer nadając mu nazwę `` Clavaria flavescens '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1974 r. Ronald H. Petersen przenosząc go do rodzaju `` Ramaria '' .
Nazwę polska podają niektóre atlasy grzybów .
Morfologia .
