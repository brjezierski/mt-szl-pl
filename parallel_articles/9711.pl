Koralówka gruba
Koralówka gruba ( `` Ramaria largentii '' Marr & amp ; D.E .
Stuntz ) – gatunek grzybów z rodziny siatkoblaszkowatychh ( `` Gomphaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramaria , Gomphaceae , Gomphales , Phallomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy nazwy naukowej :
Nazwę koralówka gruba podają niektóre atlasy grzybów .
Dawniej nazywany był gałęziakiem grubym .
Morfologia .
Należy do największych koralówek górskich lasów iglastych .
Bogato rozgałęziony , dochodzi do 17 ( 20 ) centymetrów wysokości .
Intensywnie żółty lub pomarańczowożółty , w miejscach skaleczeń oraz na końcach gałązek cytrynowożółty .
