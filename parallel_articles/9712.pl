Ramat Gan
( ; / ; pol .
„ wzgórza ogrodów ” ) – miasto położone w dystrykcie Tel Awiw w Izraelu .
Leży na równinie Szaron nad Morzem Śródziemnym , w zespole miejskim Gusz Dan .
Położenie .
Ramat Gan jest położony na kontynencie azjatyckim , na wschodnim wybrzeżu Morza Śródziemnego .
Miasto leży na nadmorskiej równinie Szaron , na historycznej drodze lądowej łączącej Europę , Azję i Afrykę .
Północną granicą miasta jest rzeka Jarkon , wschodnią miasto Bnei Brak , a południową i zachodnią miasto Giwatajim .
Leży w otoczeniu miast Tel Awiw-Jafa , Giwatajim , Bene Berak , Giwat Szemu ’ el , Kirjat Ono , Or Jehuda , miasteczka Azor , oraz moszawów Gannot i Chemed .
Na wschód od miasta jest baza wojskowa Tel-Hashomer należąca do Sił Obronnych Izraela .
Podział administracyjny .
Miasto posiada liczne dzielnice : Centrum , Nachalat Ganim , Kirjat Krinici , Ramat Szikma , Ramat Jicchak , Szuchnat Riszonim , Tel Jehuda , Giwat Geula , Newe Jehoszua , Kirjat Borchow , Marom Nawe , Ramat Amidar , Ramat Chen , Szikun Watikim , Szchunat Hillel , Elite i Diamond Exchange District oraz Tel Binjamin .
Środowisko naturalne .
Miasto wybudowano na wapiennych skałach .
Ramat Gan posiada liczne parki i ogrody , wliczając w to Park Narodowy o powierzchni 1,9ha .
Klimat .
Ramat Gan ma klimat śródziemnomorski , który charakteryzuje się gorącymi i wilgotnymi latami oraz chłodnymi i deszczowymi zimami .
Wiosna rozpoczyna się w marcu , a w drugiej połowie maja rozpoczyna się lato .
Średnia temperatura latem wynosi 26 °C , a zimą 12 °C .
Opady śniegu są rzadkością w Ramat Ganie , ale zdarza się spadek temperatury do 6 °C .
Suma rocznych opadów atmosferycznych wynosi 500 mm .
