Rammstein ( album )
Rammstein – siódmy album studyjny niemieckiego zespołu muzycznego Rammstein .
Został wydany 17 maja 2019 roku nakładem wytwórni Universal Music Group .
Promocję albumu rozpoczęto w marcu 2019 roku , wraz z premierą pierwszego promującego singla – „ Deutschland ” , kompozycja dotarła w Niemczech na szczyt notowania najpopularniejszych singli oraz uzyskała status złotej płyty .
Kolejnym singlem został utwór „ Radio ” .
`` Rammstein '' zadebiutował na pierwszym miejscu notowania Offizielle Top 100 w Niemczech , rozchodząc się w pierwszym tygodniu w ilości 260 000 egzemplarzy .
Ten wyczyn sprawił , że płyta ta stał się najlepiej sprzedającym albumem w pierwszym tygodniu w Niemczech w XXI wieku .
W Polsce album dotarł do pierwszego miejsca notowania OLiS oraz uzyskał status platynowej płyty .
Lista utworów .
Wszystkie utwory zostały napisane przez członków zespołu Rammstein ( Richard Kruspe , Paul Landers , Till Lindemann , Christian Lorenz , Oliver Riedel , Christoph Schneider ) .
