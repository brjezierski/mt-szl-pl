Ramsau am Dachstein
Ramsau am Dachstein – uzdrowiskowa gmina w Austrii , w kraju związkowym Styria , w powiecie Liezen , wchodzi w skład ekspozytury politycznej Gröbming .
Liczy 2765 mieszkańców ( 1 stycznia 2015 ) .
Leży w Alpach Salzburskich .
Jest znaną bazą narciarską i ośrodkiem sportowym , znajduje się tu kompleks skoczni narciarskich Mattensprunganlage , w tym obiekt normalny , na którym odbywały się Mistrzostwa Świata w Narciarstwie Klasycznym 1999 .
Historia .
Pierwsza wzmianka o miejscowości pochodzi z roku 1120 .
Znajdowały się tu ziemie szlacheckie .
Podczas reformacji i kontrreformacji w gminie starły się w potyczkach armie obu nurtów wyznaniowych .
