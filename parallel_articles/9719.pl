Ramularia acris
Ramularia acris Lindr .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju jaskier ( `` Ranunculus '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy opisano gatunek we Francji .
Holotyp na `` Ranunculus breyninus '' .
W polskim piśmiennictwie mykologicznym `` Ramularia acris '' jest synonimizowany z `` Septocylindrium ranunculi '' Peck , `` Ramularia aequivoca '' ( Ces . )
Sacc .
f. `` ranunculi-acris '' Massalongo , według Index Fungorum są to jednak odrębne gatunki .
Charakterystyka .
Na porażonych roślinach powoduje powstawanie nieokrągłych , nieregularnych plam , czasem ograniczonych do nerwacji liścia , czasami rozproszonych , o średnicy 2–6 mm .
Czasami sąsiednie plamy zlewają się .
