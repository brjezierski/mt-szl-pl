Charakterystyka .
Na porażonych roślinach powoduje powstawanie niewielkich plam o średnicy 2–5 mm .
Początkowo są zielonawe , potem zielonobrązowe , w środku żółte , często koncentrycznie strefowane .
Czasami występują tak gęsto i zlewają się z sobą , że liść wygląda jak porażony przez mączniaka prawdziwego .
Zarodniki tworzą się na obydwu stronach liści w postaci nalotu .
Endofit , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory 2–3–komórkowe , o wymiarach 29–83 μm .
Konidia powstają pojedynczo , bardzo rzadko w łańcuszkach .
Są podłużnie jajowate lub cylindryczne , zwykle 2– , rzadziej 3– lub 4–komórkowe , o wymiarach 16–37 × 4,5–7 μm .
Występowanie .
Znane jest występowanie `` Ramularia agrestis '' głównie w Europie , podano też jego występowanie w północno-wschodniej części USA i Chinach .
W Polsce podano jego występowanie na następujących gatunkach : fiołek ałtajski ( `` Viola altaica '' ) , fiołek polny ( `` Viola arvensis '' ) , fiołek trójbarwny ( `` Viola tricolor '' ) .
Poza Polską notowany także na fiołku dackim ( `` Viola dacica '' ) .
Jest monofagiem występującym na niewielu gatunkach fiołków .
