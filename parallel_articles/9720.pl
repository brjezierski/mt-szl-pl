Ramularia agrestis
Ramularia agrestis Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na niektórych gatunkach fiołków ( `` Viola '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
