Ramularia alnicola
Ramularia alnicola Cooke – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju olsza ( `` Alnus '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Po raz pierwszy opisano gatunek na olszy czarnej ( `` Alnus glutinisa '' ) w Anglii .
Charakterystyka .
Na porażonych roślinach powoduje powstawanie okrągławych plam o średnicy do 8 mm .
Początkowo są one brązowoszare , potem brązowoczarne .
Podczas wilgotnej pogody na obydwu stronach liści pojawia się nalot konidioforów i zarodników , bardziej obfity na dolnej stronie liści .
Endofit , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory zazwyczaj jedno- lub dwukomórkowe , rzadziej trzykomórkowe , o wymiarach 13–27 × 2,3–2,5 μm .
Na ich końcach powstają w łańcuszkach jedno- lub dwukomórkowe zarodniki konidialne o cylindrycznym lub wrzecionowatym kształcie i wymiarach 6–18 ( –20 ) × 2–2,3 ( –2,5 ) μm .
Występowanie .
Odnotowano występowanie w Ameryce Północnej ( USA ) i Europie .
W polskiej literaturze mykologicznej do 2003 r. podano 2 stanowiska , obydwa na olszy szarej ( `` Alnus incana '' ) .
