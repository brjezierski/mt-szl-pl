Ramularia asteris
Ramularia asteris ( W. Phillips & amp ; Plowr . )
Bubák – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na niektórych gatunkach roślin z rodziny astrowatych .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1877 r. W. Phillips i Ch.B .
Plowright nadając mu nazwę `` Fusidium asteris '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu František Bubák w 1908 r .
Holotyp : `` Aster tripolium '' .
Synonimy :
Występowanie .
Występuje w Azji , Europie i Ameryce Północnej .
Zanotowano występowanie na gatunkach roślin zaliczanych do rodzajów : aster ( `` Aster '' ) , ożota ( `` Galatella '' ) , doględka ( `` Grindelia '' ) , `` Heteropappus '' , rudbekia ( `` Rudbeckia '' ) , nawłoć ( `` Solidago '' ) .
W Polsce rodzaj `` Ramularia '' należy do słabo zbadanych .
Do 2003 r. notowano występowanie `` R. asteris '' tylko na astrze gawędka ( `` Aster amellus '' ) .
