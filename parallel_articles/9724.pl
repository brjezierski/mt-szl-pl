Ramularia bistortae
Ramularia bistortae Fuckel – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na rdeście wężowniku ( `` Polygonum bistorta '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Karl Wilhelm Gottlieb Leopold Fuckel w 1870 r. Synonim : `` Ovularia bistortae '' ( Fuckel ) Sacc .
1886 .
Charakterystyka .
Na porażonych roślinach powoduje powstawanie eliptycznych lub okrągławych plam o średnicy 2–6 mm .
Są brązowawe , otoczone brązowo fioletową obwódką .
Zarodniki tworzą się na obydwu stronach liści w postaci nalotu .
Endofit , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory 1–4–komórkowe , o wymiarach 22–42 × 2–3 μm .
Konidia powstają pojedynczo , rzadziej w łańcuszkach .
Są jednokomórkowe , gruszkowate lub odwrotnie jajowate , o wymiarach 8–13 × 3–4 μm .
Znane jest występowanie `` Ramularia bistortae '' w niektórych państwach Europy .
Jest monofagiem .
W Polsce notowany tylko na rdeście wężowniku , poza Polską także na rdeście żyworodnym ( `` Bistorta vivipara '' ) .
