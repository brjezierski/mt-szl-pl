Ramularia cirsii
Ramularia cirsii Allesch .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzajów oset ( `` Carduus '' ) i ostrożeń ( `` Cirsium '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Po raz pierwszy gatunek został zdiagnozowany w 1892 r. przez Andreasa Alleschera na ostrożniu lancetowatym ( `` Cirsium vulgare '' ) w Bawarii w Niemczech .
Morfologia .
Powoduje na porażonych liściach powstawanie okrągłych lub eliptycznych plam o średnicy 2–10 mm .
Początkowo są brązowawozielonkawe , potem brązowe , w końcu brązowoczarne , a środek plam staje się biały .
Na obydwu stronach liści na plamach tych podczas wilgotnej pogody pojawia się nalot .
Składa się on z 1–2 ( –3 ) –komórkowych konidioforów o wymiarach ( 11– ) 16–46 × 2–2,5 ( –3,4 ) μm z zarodnikami powstającymi w łańcuszkach .
Zarodniki cylindryczne , podłużnie elipsoidalne lub podłużnie jajowate jedno lub dwukomórkowe o rozmiarach 9–30 × 2–2,5 μm .
Występowanie .
Odnotowano występowanie tylko w Europie .
W polskiej literaturze mykologicznej do 2003 r. opisano występowanie tego gatunku ( opisanego jako synonim `` Ramularia cynarae '' Sacc .
Emend U. Braun ) na oście nastroszonym ( `` Carduus acanthoides '' ) , oście kędzierzawym ( `` Carduus crispus '' ) , ostrożniu polnym ( `` Cirsium arvense '' ) , ostrożniu warzywnym ( `` Cirsium oleraceum '' ) .
