Ramularia didyma
Ramularia didyma Unger – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodziny jaskrowatych ( `` Ranunculaceae '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
