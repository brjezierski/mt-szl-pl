Ramularia evanida
Ramularia evanida ( J.G .
Kühn ) Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na goryczce trojeściowej ( `` Gentiana asclepiadea '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Julius Gotthelf Kühn w 1877 r. nadając mu nazwę `` Cylindrosporium evanidum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Pier Andrea Saccardo w 1886 r ..
Charakterystyka .
Na porażonych roślinach powoduje powstawanie zielonawobrązowych lub brązowych plam o średnicy 3–10 ( –15 ) mm ograniczonych użyłkowaniem liścia .
Ich środek cz czasem staje się jaśniejszy .
Często sąsiednie plamy zlewają się z sobą .
W sprzyjających patogenowi warunkach w obrębie tych plam na obydwu stronach liści pojawia się nalot złożony z konidioforów i zarodników .
Endobiont , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory jedno lub dwukomórkowe , o wymiarach 18–70 × 2,3–3 μm .
Konidia powstają w łańcuszkach .
Są zazwyczaj jednokomórkowe .
rzadziej dwukomórkowe , elipsoidalne lub cylindryczne , o wymiarach 9–23 × 2,3 μm .
`` Ramularia evanida '' w Polsce opisana została na kilku stanowiskach w Sudetach , Tatrach i Zakopanem .
