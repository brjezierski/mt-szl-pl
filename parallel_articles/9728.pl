Ramularia geranii
Ramularia geranii Fuckel – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na gatunkach roślin należących do rodzaju bodziszek ( `` Geranium '' ) .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Występowanie .
Znane jest występowanie `` R. geranii '' w Europie i Ameryce Północnej .
W Polsce jest bardzo pospolity , należy do najczęściej występujących gatunków `` Ramularia '' i jest jedynym gatunkiem tego rodzaju występującym na bodziszkach .
W piśmiennictwie naukowym podano liczne jego stanowiska .
W Polsce opisano jego występowanie na następujących gatunkach bodziszków : `` Geranium dissectum '' , `` Geranium molle '' , `` Geranium palustre '' , `` Geranium phaeum '' , `` Geranium dissectum '' , `` Geranium pratense '' , `` Geranium pusillum '' , `` Geranium rotundifolium '' , `` Geranium pyrenaicum '' , `` Geranium dissectum '' , `` Geranium robertianum '' , `` Geranium sanguineum '' , `` Geranium sylvaticum '' .
