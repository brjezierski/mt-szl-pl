Ramularia keithii
Ramularia keithii Massee – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na ślazie zaniedbanym ( `` Malva neglecta '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał George Edward Massee w 1893 r .
Synonimy :
Charakterystyka .
Na porażonych roślinach powoduje powstawanie ograniczonych nerwami , jasnobrązowych plam o średnicy około 5 mm .
Zarodniki i konidiofory tworzą się głównie na dolnych stronach liści w postaci nalotu .
Endofit , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory o wymiarach 20–100 × 2–3,5μm .
Konidia powstają w krótkich łańcuszkach .
Są wrzecionowate lub elipsoidalne , jedno lub dwukomórkowe , o wymiarach 20–40 × 4–6 μm .
Monofag .
W Polsce podano występowanie `` Ramularia keithii '' na ślazie zaniedbanym ( `` Malva neglecta '' ) .
