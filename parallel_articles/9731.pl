Ramularia lamii
Ramularia lamii Fuckel – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na gatunkach roślin należących do rodziny jasnotowatych ( `` Lamiaceae '' ) .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Lektotyp ; na jasnocie ( `` Lamium '' ) – Fuckel , Fungi rhen .
136 ( HAL ) .
Występowanie .
`` Ramularia lamii '' występuje w Azji , Europie , Ameryce Północnej , Afryce , Nowej Zelandii .
W Polsce jest pospolita .
Pasożyt obligatoryjny .
Występuje na wielu gatunkach roślin z rodziny jasnotowatych .
