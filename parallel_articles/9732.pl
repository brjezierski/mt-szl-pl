Ramularia lamiicola
Ramularia lamiicola C. Massal .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju jasnota ( `` Lamium '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Caro Benigno Massalongo w 1890 r. na żywych liściach jasnoty białej we Włoszech
`` Ramularia lamiicola '' znana jest w niektórych krajach Europy .
W piśmiennictwie polskim jest uważana za synonim `` Ramularia lamii '' Fuckel , jednak według Index Fungorum są to odrębne gatunki .
