Ramularia lapsanae
Ramularia lapsanae ( Desm . )
Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na łoczydze pospolitej ( `` Lapsana communis '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Jean Baptiste Henri Joseph Desmazières w 1851 r. nadając mu nazwę `` Oidium fusisporioides '' var .
`` lapsanae '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Pier Andrea Saccardo w 1881 r .
