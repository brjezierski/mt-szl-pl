Ramularia major
Ramularia major ( Unger ) U. Braun – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny pasożytujący na gatunkach roślin należących do rodzajów miłosna , lepiężnik i podbiałek .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1833 r. F. Unger nadając mu nazwę `` Cylindrosporium majus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Uwe Braun w 1833 r ..
Występowanie .
Pasożyt obligatoryjny .
W Polsce występuje na miłosnej górskiej ( `` Adenostyles alliariae '' ) , podbiałku alpejskim ( `` Homogyne alpina '' ) , lepiężniku białym ( `` Petasites alba '' ) , lepiężniku różowym ( `` Petasites hybridus '' ) , lepiężniku wyłysiałym ( `` Petasites kablikianus '' ) i lepiężniku kutnerowatym ( `` Petasites spurius '' ) .
