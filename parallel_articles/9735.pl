Ramularia pratensis
Ramularia pratensis Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju rabarbar ( `` Rheum '' ) , szczaw ( `` Rumex '' ) i szczawiór ( `` Oxyria '' ) .
Powoduje u nich plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Holotyp : na szczawiu zwyczajnym ( `` Rumex acetosa '' ) , Włochy , Padwa .
W polskim piśmiennictwie za synonim `` Ramularia pratensis '' Sacc .
uważany jest `` Ramularia rhei '' Allesch. , jednak według Index Fungorum są to odrębne gatunki .
Charakterystyka .
Na porażonych roślinach powoduje powstawanie na obydwu powierzchniach blaszki liściowej okrągławych , lub nieregularnych i lekko kanciastych plam o średnicy do 10 mm o barwie początkowo żółtawo-ochrowej , potem jasnobrązowej , w końcu białoszarej .
Plamy otoczone są wąskim lub szerokim , czerwonawo-brązowym do fioletowego obrzeżem , sporadycznie nieco strefowanym .
Sąsiednie plamy czasami zlewają się .
