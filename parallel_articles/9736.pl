Ramularia primulae
Ramularia primulae Thüm .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Wywołuje plamistość liści pierwiosnka .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonim : `` Cylindrosporium primulae '' ( Thüm . )
J. Schröt .
1908 .
Morfologia .
W miejscach rozwoju grzybni `` Ramularia primulae '' na obydwu stronach liści porażonych roślin tworzą się mniej więcej okrągłe , kanciaste i nieregularne plamy o średnicy 1-10 mm .
Początkowo są żółtawo-ochrowe , potem coraz ciemniejsze : żółtawo-ochrowe , brązowe , szarobeżowe , w końcu szare .
Obrzeże rozmyte , ciemniejsze , otoczone żółtawą plamą , często plamy te zlewają się .
Grzybnia rozwija się w tkance miękiszowej wewnątrz liści .
Strzępki bezbarwne , z przegrodami , rozgałęzione .
