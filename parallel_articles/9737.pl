Ramularia rhabdospora
Ramularia rhabdospora ( Berk .
& amp ; Broome ) Nannf .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju babka ( `` Plantago '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisali Miles Joseph Berkeley i Christopher Edmund Broome w 1875 r. nadając mu nazwę `` Cylindrosporium rhabdosporum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu John Axel Nannfeldt w 1950 r .
Synonimy :
Charakterystyka .
Na porażonych roślinach powoduje powstawanie nieregularnych , mniej więcej eliptycznych plam o wymiarach 2–5 mm .
Są słabo widoczne , początkowo szarozielone , potem zielonobrązowe lub zielonoczarne i czasami zlewają się ze sobą zajmując dużą część liścia .
Przy sprzyjającej patogenowi pogodzie tworzy się nalot złożony z konidioforów i zarodników .
Powstaje on na obydwu stronach liści , ale częściej na dolnej .
Endobiont , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory 1–3–komórkowe , o wymiarach ( 11– ) 27–46 × ( 2,5– ) 3,4–4,6 μm .
Konidia powstają w łańcuszkach .
Są 1–4–komórkowe , cylindryczne lub podłużnie jajowate , o wymiarach 16–40 × 2,5–4,6 μm .
W mikroskopie skaningowym mają wyraźnie kolczastą powierzchnię .
`` Ramularia rhabdospora '' znana jest w Europie , Australii i na Nowej Zelandii .
Monofag , w Polsce jego występowanie opisano tylko na babce lancetowatej ( `` Plantago lanceolata '' ) i babce zwyczajnej ( `` Plantago major '' ) ( na tym gatunku opisano `` Ramularia plantaginis '' Ellis & amp ; G. Martin , ale według Index Fungorum jest to synonim `` Ramularia rhabdospora '' ) .
