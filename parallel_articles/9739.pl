Ramularia rubella
Ramularia rubella ( Bonord . )
Nannf .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1861 r. Hermann Friedrich Bonorden nadając mu nazwę `` Crocysporium rubellum `` .
Do rodzaju `` Ramularia '' przeniósł go Pier Andrea Saccardo w 1886 r. Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1950 r. John Axel Nannfeldt .
Synonimy :
Morfologia .
W miejscach rozwoju grzybni `` Ramularia rubella '' na liściach roślin tworzą się mniej więcej okrągłe , kanciaste i nieregularne plamy o średnicy 1-20 mm .
Początkowo są bladozielonawe , potem coraz ciemniejsze : żółtawo-ochrowe , brązowe , szarobrązowe , w końcu szare .
