Agaricus bresadolanus
Agaricus bresadolanus Bohus – gatunek grzybów z rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Klasyfikacja według Index Fungorum : `` Agaricus , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisał go w 1969 r. G. Bohus na Węgrzech .
