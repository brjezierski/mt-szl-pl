Ramularia sambucina
Ramularia sambucina Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju bez ( `` Sambucus '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Charakterystyka .
Na porażonych roślinach powoduje powstawanie okrągławych lub eliptycznych plam o wymiarach 5–15 × 5–10 mm .
Początkowo są one zielonkawe , potem kolejno zielonobrązowe i brązowe z ciemniejszym obrzeżem .
Jeszcze później środek plamy staje się białawy .
Podczas wilgotnej pogody na obydwu stronach liści pojawia się nalot konidioforów i zarodników , bardziej obfity na dolnej stronie liści .
Endofit , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory zazwyczaj jedno- , rzadziej dwukomórkowe , o wymiarach ( 9– ) 16–42 × 2,5–4,6 μm .
Na ich końcach powstają w łańcuszkach zazwyczaj dwukomórkowe , czasami jedno- lub trzykomórkowe zarodniki konidialne o cylindrycznym , wrzecionowatym lub elipsoidalnym kształcie i wymiarach 16–37 × ( 2,5– ) 3–7 μm .
Występowanie .
Odnotowano występowanie w Ameryce Północnej ( ( USA ) , Europie , oraz Afryce Północnej ( Maroko ) .
W polskiej literaturze mykologicznej podano dość liczne stanowiska tego fitopatogenu na bzie czarnym ( `` Sambucus nigra '' ) , bzie koralowym ( `` Sambucus racemosa '' ) i bzie hebdzie ( `` Sambucus ebulus '' ) .
