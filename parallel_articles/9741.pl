Ramularia scrophulariae
Ramularia scrophulariae Fautrey & amp ; Roum .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju trędownik ( `` Scrophularia '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisali François Fautrey i Casimir Roumeguère w 1891 r .
W piśmiennictwie polskim gatunek ten uważany jest za synonim `` Ramularia carneola '' ( Sacc . )
Nannf. , jednak według Index Fungorum są to odrębne gatunki .
`` Ramularia scrophulariae '' znana jest w licznych krajach Europy i w Maroku .
