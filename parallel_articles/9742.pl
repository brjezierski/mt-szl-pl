Ramularia succisae
Ramularia succisae Sacc .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju czarcikęs ( `` Succisa '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Pier Andrea Saccardo w 1882 r .
Synonimy :
Charakterystyka .
Na porażonych roślinach powoduje powstawanie brązowoczarnych , wielokątnych plam o okrągławym zarysie iśrednicy 5–10 mm .
Czasami zlewają się ze sobą zajmując dużą część liścia .
Przy sprzyjającej patogenowi pogodzie tworzy się nalot złożony z konidioforów i zarodników .
Powstaje on na obydwu stronach liści , ale częściej na dolnej .
Endobiont , jego grzybnia jest zanurzona w tkankach rośliny .
Konidiofory 1–4–komórkowe ( z rzadka zdarzają się także 5–komórkowe ) o wymiarach 13–42 ( –70 ) × 2,3–3,4 μm .
Konidia powstają w łańcuszkach .
Zazwyczaj są dwukomórkowe , rzadziej jedno– lub trzykomórkowe , cylindryczne lub elipsoidalne , o wymiarach 11–30 × 2,3–2,5 ( –3,4 ) μm .
`` Ramularia succisae '' znana jest w licznych krajach Europy .
Monofag , w Polsce jego występowanie opisano tylko na czarcikęsie łąkowym ( `` Succisa pratense '' ) .
