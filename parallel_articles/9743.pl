Ramularia taraxaci
Ramularia taraxaci P. Karst .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Pasożyt roślin , powodujący powstawanie plam na liściach porażonych roślin .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
W piśmiennictwie polskim traktowany jako synonim `` Ramularia inaequalis '' ( Preuss ) U. Braun 1998 ( pisownia : `` Ramularia inaequale '' ( Preuss ) U. Braun ) .
Według Index Fungorum jednak jest to odrębny gatunek .
Morfologia .
W miejscu rozwoju grzybni na porażonych liściach roślin powstają mniej więcej okrągłe , rzadziej eliptyczne plamy o średnicy do 1 cm .
Początkowo są zielonkawe , potem kolejno zielonobrązowe , brązowe , w końcu czarne .
Często posiadają ciemniejszą , purpurowofioletową otoczkę .
Nalot tworzy się na obydwu stronach liści .
Grzybnia rozwija się wewnątrz tkanek porażonych roślin .
Konidiofory 1-3-komórkowe o długości 6–46 ( –70 ) μm i średnicy ( 2– ) 2,3–4,6 μm .
Konidia tworzą się w łańcuszkach .
Są 1–4–komórkowe , cylindryczne , elipsoidalne lub jajowate , o rozmiarach 6–46 ( –60 ) × ( 2– ) 2,3–2,5 ( –3,4 ) μm .
Występowanie .
