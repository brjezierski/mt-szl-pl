Ramularia ulmariae
Ramularia ulmariae Cooke – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju wiązówka ( `` Filipendula '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Mordecai Cubitt Cooke w 1876 r .
Synonimy :
W piśmiennictwie polskim Agata Wołczańska traktuje `` Ramularia ulmariae '' jako synonim `` Ramularia spiraeae-arunci '' ( Sacc . )
Allescher , jednak według Index Fungorum są to odrębne gatunki .
`` Ramularia ulmariae '' znana jest w niektórych krajach Europy oraz na wschodnim wybrzeżu Kanady .
Monofag .
W Polsce jego występowanie opisano tylko na wiązówce błotnej ( `` Filipendula ulmaria '' ) .
