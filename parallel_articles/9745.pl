Ramularia urticae
Ramularia urticae Ces .
– gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na roślinach z rodzaju pokrzywa ( `` Urtica '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Synonimy :
Charakterystyka .
Na porażonych roślinach powoduje powstawanie na obydwu powierzchniach blaszki liściowej ciemnobrązowych lub czarnych plam rozrzuconych po całym liściu .
Są one nieregularne , zazwyczaj kanciaste i wokół nich często powstają chlorotyczne obszary .
Plamy mają długość 1–12 mm , średnicę 1–5 mm .
Grzybnia patogenu jest zanurzona w tkankach rośliny .
Strzępki hialinowe , septowane , słabo rozgałęzione .
Konidiofory w małych lub dość licznych pakietach .
Luźno , gęsto lub bardzo gęsto wyrastają z tkanek rośliny na zewnątrz poprzez aparaty szparkowe .
Są wyprostowane , niemal cylindryczne , proste lub nieco faliste , rzadko rozgałęzione .
Mają rozmiar 10–85 × ( 1,5– ) 2,5–6 ( –7 ) μm , są hialinowe , gładkie , bez przegród lub słabo septowane .
Po oderwaniu się zarodników pozostają ciemniejsze blizny .
Konidia tworzą się pojedynczo , rzadko w łańcuszkach .
Mają kształt elipsoidalny , jajowaty lub cylindryczny i rozmiary ( 6– ) 8–35 ( –40 ) × ( 2– ) 3–7 ( –10,5 ) μm .
Są hialinowe , o powierzchni gładkiej lub delikatnie chropowatej .
Zawierają zagęszczoną , nieco ciemniejszą cytoplazmę .
Występowanie .
Odnotowano występowanie w Ameryce Północnej , Europie , Azji i Nowej Zelandii .
W polskiej literaturze mykologicznej podano kilkadziesiąt opisów występowania tego fitopatogenu na pokrzywie zwyczajnej i pokrzywie żegawce na terenie całej Polski .
