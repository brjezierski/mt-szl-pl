Ramularia variabilis
Ramularia variabilis Fuckel – gatunek grzybów z klasy `` Dothideomycetes '' .
Grzyb mikroskopijny , fitopatogen pasożytujący na gatunkach roślin zaliczanych do rodzajów dziewanna ( `` Verbascum '' ) i naparstnica ( `` Digitalis '' ) .
Powoduje plamistość liści .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ramularia , Mycosphaerellaceae , Capnodiales , Dothideomycetidae , Dothideomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Leopold Fuckel w 1870 r .
Synonimy :
Charakterystyka .
Na porażonych roślinach powoduje powstawanie okrągławych lub wielokątnych , zielonkawobrązowych , brązowych lub brązowoczarnych plam , zazwyczaj z fioletowobrązową obwódką .
Mają średnicę 2–15 mm i czasami zlewają się z sobą .
W sprzyjających patogenowi warunkach w obrębie tych plam na obydwu stronach liści pojawia się nalot złożony z konidioforów i zarodników .
