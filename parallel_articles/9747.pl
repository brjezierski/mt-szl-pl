Ramzan Kadyrow
Ramzan Achmatowicz Kadyrow , ( ur .
5 października 1976 we wsi Centaroj ) – czeczeński polityk , premier Czeczenii w latach 2006–2007 , od 2007 do marca 2011 jej prezydent , od 2011 – Szef Republiki Czeczeńskiej ( ros .
Главa Чеченской Республики ) .
Syn Achmata .
Oskarżany o popełnienie zbrodni wojennych na Ukrainie .
Życiorys .
W I wojnie w Czeczenii walczył z Rosjanami , w II wojnie wraz z ojcem stanął po ich stronie .
Po dojściu Achmata Kadyrowa do władzy zorganizował i dowodził służbą bezpieczeństwa złożoną głównie z amnestionowanych byłych separatystów , która stała się de facto prywatną armią klanu Kadyrowów .
Dosłużył się stopnia podpułkownika milicji .
Dziennikarka rosyjskiej opozycyjnej „ Nowej Gaziety ” Anna Politkowska twierdziła , że w swoim domu we wsi Centaroj ( Chosi-Jurt ) urządził katownię dla przeciwników ojca .
Zarzucała mu także inspirowanie morderstw , udział w strzelaninach i burdach .
Za główne źródło dochodów Kadyrowa Politkowska uważała porwania ludzi dla okupu .
Uważa się go za człowieka Putina w Czeczenii .
W rosyjskich wyborach prezydenckich w marcu 2004 stał na czele regionalnego sztabu wyborczego .
Trzy godziny po zamachu na jego ojca Achmata Kadyrowa został przyjęty przez Putina na Kremlu .
Dzień później ( 10 maja 2004 ) został mianowany wicepremierem i faktycznie kierował rządem w Czeczenii – po przejęciu przez Siergieja Abramowa obowiązków prezydenta .
W lutym 2006 wybrany przewodniczącym republikańskiego oddziału partii Zjednoczona Rosja , najliczniejszej obecnie partii politycznej w Czeczenii , jak i w całej Rosji .
15 lutego 2007 Ramzan Kadyrow po podaniu się do dymisji przez Ału Ałchanowa został mianowany pełniącym obowiązki prezydenta Czeczenii .
Zaprzysiężono go oficjalnie 5 kwietnia 2007 .
W dniu 5 marca 2011 r. Parlament Czeczenii zatwierdził Kadyrowa na stanowisku Szefa Republiki Czeczeńskiej .
W 2009 otrzymał stopień generała majora rosyjskiej Policji .
W lipcu 2020 został mianowany generałem majorem Sił Zbrojnych Federacji Rosyjskiej .
W marcu 2022 roku ogłoszono jego awans na generała porucznika w wojsku rosyjskim .
W październiku tego samego roku został awansowany do stopnia generała pułkownika .
Objął funkcję honorowego prezesa klubu piłkarskiego Achmat Grozny .
W 2014 otrzymał czarny pas w karate .
Żonaty , ma dwanaścioro dzieci , w tym dwoje adoptowanych .
Jego córki Chadidża i Ajszat objęły stanowiska urzędnicze w oświacie Republiki Czeczeńskiej .
