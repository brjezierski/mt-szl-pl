Rangun ( prowincja )
Rangun ( // ) – jedna z prowincji w Mjanmie ( Birmie ) , ze stolicą w Rangunie .
Prowincję zamieszkuje 7 355 075 osób , z czego 70,1 % zasiedla tereny zurbanizowane .
Oprócz Birmańczyków duży odsetek mieszkańców prowincji stanowią Karenowie ( zwłaszcza w okręgu miejskim Insein ) oraz Szanowie , Monowie , osoby pochodzenia indyjskiego i chińskiego .
Spośród 45 okręgów miejskich składających się na prowincję , 34 znajdują się w obrębie granic Rangunu stanowiąc jego dzielnice .
