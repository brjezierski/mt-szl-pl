Raphaël Varane
Raphaël Xavier Varane ( ur .
25 kwietnia 1993 w Lille ) – francuski piłkarz martynikańskiego pochodzenia , występujący na pozycji obrońcy .
W latach 2013–2022 reprezentant Francji .
Mistrz Świata z 2018 roku .
Wicemistrz Świata z 2022 roku .
Uczestnik Mistrzostw Świata 2014 i Mistrzostw Europy 2021 .
Kariera klubowa .
RC Lens .
Karierę piłkarską rozpoczął w wieku 7 lat , w klubie AS Hellemmes .
Trenował w nim przez 2 lata .
W 2002 zainteresowało się nim kilka znaczących klubów .
