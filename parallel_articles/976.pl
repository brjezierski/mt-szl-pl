Agaricus phaeolepidotus
Agaricus phaeolepidotus ( F.H .
Møller ) – gatunek grzybów należący do rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Agaricus , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Synonimy nazwy naukowej :
Morfologia i biologia .
Saprotrof , wytwarzający toksyczne dla człowieka owocniki z białawym , pokrytym pękającą na drobne łuski brązową skórką , w centrum ciemniejszym kapeluszem ( średnicy 5–10 cm ) o blaszkowatym hymenoforze , umieszczonym na białawym , nagim , bulwiastym u podstawy trzonie ze zwisającym pierścieniem .
Charakterystyczną cechą `` Agaricus phaeolepidotus '' jest ( czasami słaba ) fenolowa woń miąższu i brązowe obrzeżenie wewnętrznej strony pierścienia .
Gatunek ten występuje lasach liściastych i mieszanych lub w parkach .
Owocniki wytwarza od lipca do września .
Spożycie owocników powoduje ( nie u wszystkich ) zaburzenia trawienne , nudności i wymioty , silniejsze w połączeniu z alkoholem etylowym .
