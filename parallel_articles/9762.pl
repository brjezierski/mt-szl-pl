Rzepak
Rzepak ( `` Brassica napus '' L. var .
`` napus '' ) – odmiana kapusty rzepak .
Występuje tylko w uprawie .
Dość często dziczeje z upraw , ale jest tylko efemerofitem .
Status we florze Polski : efemerofit , kenofit .
Prawdopodobnie jest mieszańcem kapusty warzywnej ( `` Brassica oleracea '' ) i kapusty właściwej ( `` Brassica rapa '' ) .
Jest uprawiany głównie w krajach Eurazji .
Biologia i ekologia .
Roślina jednoroczna lub dwuletnia .
Szacuje się , że rzepak ozimy potrzebuje jesienią około 75–80 dni z temperaturą powyżej 5 stopni Celsjusza , aby odpowiednio przygotować się do zimy , co oznacza , że termin siewu w północno-wschodniej Polsce przypada na 5 – 10 sierpnia , a w zachodniej i południowej 20 – 25 sierpnia .
Kiełkuje po 7 dniach od wysiewu .
Przed zimą zdąży jeszcze wytworzyć skróconą łodygę z 8-10 liśćmi .
Kwitnie w następnym roku przez około 45 dni .
Kwiaty zapylane są przez pszczoły .
Łuszczyna pęka dość łatwo , stąd też przy zbiorze rzepaku mogą wystąpić dość duże straty .
Rzepak ozimy stanowi ważny element płodozmianu i zostawia po sobie zasobne stanowisko ( duża ilość materii organicznej oraz resztki pożniwne bogate w potas ) , które najlepiej nadaje się pod uprawę zbóż .
Uprawa .
Uprawiany jest w wielu krajach świata w średnich szerokościach geograficznych ( klimat umiarkowany i podzwrotnikowy ) .
Występuje w dwóch formach : jako rzepak jary ( roślina jednoroczna ) i rzepak ozimy ( roślina dwuletnia ) .
W Polsce uprawia się głównie rzepak ozimy i jego uprawy zajmują 95 % powierzchni upraw wszystkich roślin oleistych .
W Chinach jest uprawiany w duży ilościach w dorzeczu Jangcy , jako uprawa zimowa .
W Indiach jako uprawa na gruntach nienawadnianych .
W Europie , zwłaszcza w okresie II wojny światowej , olej rzepakowy zajął poważne miejsce na rynku jako produkt spożywczy , a także jako olej techniczny .
Największym producentem w Europie jest Francja a zaraz za nią Niemcy .
