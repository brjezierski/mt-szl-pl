Ratusz w Prudniku
Ratusz w Prudniku – zabytkowy ratusz miejski w Prudniku zbudowany w 1782 roku w stylu barokowo–klasycystycznym .
Mieści się pośrodku Rynku , w centralnej części Starego Miasta .
Historia .
Pierwszy ratusz w Prudniku został wzniesiony w XIII lub XIV wieku , prawdopodobnie z drewna .
Został spalony przez Szwedów w 1627 roku .
Kolejny budynek miał charakter barokowy i był niszczony w latach 1650 , 1653 , 1735 i 1779 .
22 listopada 1642 z Opola do Prudnika wkroczyli Szwedzi .
Zamierzali spalić miasto , ale dzięki negocjacjom do tego nie doszło .
Podczas najazdu uszkodzeniu uległa wieża ratuszowa , którą wyremontowano dopiero w 1653 .
Odrestaurowana wieża uległa ponownemu uszkodzeniu .
W sierpniu 1690 przeprowadzono jej powtórny remont od góry do krużganku według planu Martina Doleczcka ze Strzelec Opolskich .
Według opisu ratusza z 1725 , na jego piętrze odbywały się posiedzenia Rady Miejskiej , a w piwnicy mieściły się ławki mięsne .
Powstały w wyniku austriackiego ostrzału 28 lutego 1779 w trakcie wojny o sukcesję bawarską pożar strawił ratusz wraz z zegarem i wieżą , którą na krótko przedtem odrestaurowano .
Brakowało wody do gaszenia ognia , ponieważ w przeciągu 2 miesięcy nie padał deszcz , a śniegu było niewiele .
Po pożarze ratusz został odbudowany od podstaw według planów architekta Thomasa ze Świdnicy .
Jego budowę ukończono w październiku 1782 .
W latach 1840–1842 podwyższono ratusz o półtora kondygnacji , pokryto go blachą cynkową i zaopatrzono w marmurowe schody .
Drugie piętro ratusza , które od tej pory zajmował sąd , oraz attykę wzniesiono nakładem kosztów wysokości 7000 talarów .
Podczas uroczystości 18 września 1842 , na wieży osadzono gałkę , flagę oraz koronę .
Krzyż osadzony na gałce wskazywał strony świata .
W latach 1855–1859 wyremontowano jego wnętrze .
W 1856 podwyższono wieżę ratuszową o jedno piętro i zaopatrzono ją w wygięty dach .
Podwyższeniu uległa część rozpoczynająca się od mieszkania strażnika wieżowego , aż do zewnętrznego gzymsu .
Prace z drewnem wykonali mistrzowie ciesielscy Carl i Friedrich Zeißner , natomiast prace kotlarskie kotlarz Hammetter .
W gałce na szczycie umieszczono różne dokumenty wraz z rysunkiem wieży , przedstawiającym jej kształt w latach 1779–1856 .
Planowana przebudowa ratusza została przerwana wybuchem I wojny światowej .
Od 1929 do 1945 dwie sale ratusza zajmowało muzeum miejskie .
Ratusz nie ucierpiał podczas bombardowania Rynku przez lotnictwo radzieckie w 1945 .
Na przełomie maja i czerwca 1945 grupa Czechów przybyła do Prudnika i wywiesiła czechosłowacką flagę na ratuszu .
Ogłosili przyłączenie Prudnika do Czechosłowacji .
Zostali przegonieni przez polskich żołnierzy bez strzelaniny .
Do lat 60 .
XX wieku budynek ratusza był porośnięty bluszczem .
Podczas prac 24 marca 2000 roku z kuli na iglicy wieży wyjęto metalową , zalutowaną kapsułę , w której znajdowały się dokumenty z 1842 .
Dotyczyły one ówczesnego remontu wieży , zawierały listę fundatorów i ówczesnych władz miasta .
Umieszczono w niej również lokalną gazetę , kilkanaście monet , a także kawałek ptasiej kości .
Znalezisko zostało zdeponowane w Muzeum Ziemi Prudnickiej .
Do kuli na iglicy trafiły jego kopie , a także druga kapsuła ze współczesnymi dokumentami , egzemplarzem lokalnej gazety i filmem ukazującym miasto i obrady Rady Miejskiej z 2000 roku .
W 2004 prudnicki ratusz został laureatem w ogólnopolskim konkursie „ Zabytek Zadbany ” organizowanym przez Narodowy Instytut Dziedzictwa .
Architektura .
Ratusz liczy trzy kondygnacje oraz poddasze oraz jest częściowo podpiwniczony .
Od północnej strony nad budynkiem dominuje przylegająca do niego klasycystyczna , wieloczłonowa wieża , zwieńczona hełmem i iglicą .
Wieża mierzy 63 metry wysokości .
W ratuszu mieści się Urząd stanu cywilnego , Gminny Zarząd Oświaty i Wychowania , a także Biuro Euroregionu Pradziad .
Nad drzwiami na wieży ratusza umieszczona jest marmurowa tablica przypominająca o odbudowie miasta przez króla Fryderyka II .
Niemiecki napis na tablicy głosi : „ Królewskie miasto Prudnik na Górnym Śląsku , chwalebne i czcigodne , sławne dzięki handlowi , zostało obrócone w popiół i zniszczone 28 lutego 1779 roku w wyniku wściekłej napaści wroga .
Spalony został także stary ratusz .
Pod hojną opieką wielkiego , dostojnego , niezwyciężonego króla Prus , Fryderyka II , wyrozumiałego i litościwego , szczęśliwego ojca narodu , miasto odzyskało widoczny teraz blask i powstało szczęśliwie od nowa wraz z wieżą w roku Pańskim 1782 . ”
Przed budynkiem stoi fontanna z 1696 roku .
