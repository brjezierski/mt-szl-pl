Ratnapura
Ratnapura ( syng .
රත්නපුර , tamil .
இரத்தினபுரி ) – stolica prowincji Sabaragamuwa w Sri Lance , leży ok. 101 km na wschód od Kolombo .
Duży ośrodek wydobycia i handlu kamieniami szlachetnymi : rubinami i szafirami .
W pobliżu znajdują się kopalnie wydobywające minerały oraz duże uprawy ryżu , owoców , herbaty i kauczukowca .
W 1901 r. populację Ratnapury liczono na 4,084 osób , w 2001 wzrosła do 46,309 .
