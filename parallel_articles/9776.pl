Rawalpindi
Rawalpindi ( urdu راولپنڈی , `` Rāvalpinḍī '' ) – miasto w północnym Pakistanie , w prowincji Pendżab , 14 km na południe od stolicy kraju Islamabad , z którą tworzy jedną , bicentryczną algomerację miejską , zamieszkałą przez ponad 4,1 miliona ludzi .
Historia .
Powstało w miejscu osady Rawalów .
W XIV w. zostało zniszczone przez Mongołów .
W II połowie XVIII w. szybki rozwój i centrum osadnictwa Sikhów .
1849 opanowane przez Brytyjczyków .
W 1919 zawarto tu układ , w którym Wielka Brytania uznała niepodległy Afganistan .
W latach 1959-1971 była to stolica Pakistanu .
