Rawicz ( herb szlachecki )
Rawicz – polski herb szlachecki , noszący zawołanie `` Rawa '' .
Wzmiankowany w najstarszym zachowanym do dziś polskim herbarzu , `` Insignia seu clenodia Regis et Regni Poloniae '' , spisanym przez historyka Jana Długosza w latach 1464–1480 .
Rawicz jest jednym z 47 herbów adoptowanych przez bojarów litewskich na mocy unii horodelskiej z 1413 roku '' '' .
Herb występował głównie wśród rodzin osiadłych w ziemi krakowskiej i sandomierskiej .
Spośród najbardziej znanych rodów pieczętujących się herbem Rawicz , należy wymienić Dembińskich i Ożarowskich .
