Razer
Razer Inc. – amerykańskie przedsiębiorstwo zajmujące się produkcją akcesoriów komputerowych dla graczy .
Znajduje się w czołówce producentów myszy i klawiatur .
Do jego najpopularniejszych produktów można zaliczyć m.in .
