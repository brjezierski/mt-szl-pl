Agaricus subfloccosus
Agaricus subfloccosus ( J.E .
Lange ) Hlaváček – gatunek grzybów należący do rodziny pieczarkowatych ( `` Agaricaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Agaricus , Agaricaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1926 r. Jakob Emanuel Lange nadając mu nazwę `` Psalliota hortensis f. subfloccosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1951 r. Hlaváček .
Synonimy nazwy naukowej :
Morfologia .
Saprotrofy wytwarzające jadalne owocniki z szarobiaławym , w centralnej części brązowawym kapeluszem ( średnicy 4–8 cm ) o blaszkowatym hymenoforze , umieszczonym na bladym trzonie , do którego przyrośnięty jest dość gruby , trwały , wzniesiony pierścień .
Gatunek ten występuje w lasach iglastych , często pod świerkami , owocniki wytwarza od lipca do października .
