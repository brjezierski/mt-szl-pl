Racibory ( stacja kolejowa )
Racibory – stacja kolejowa w województwie podlaskim , w Polsce znajdująca się na obszarze dwóch wsi : Nowych Raciborów w gminie Sokoły w powiecie wysokomazowieckim oraz Porośli-Wojsławów w gminie Poświętne w powiecie białostockim .
Według klasyfikacji PKP ma kategorię dworca lokalnego .
Znajdują się tu 2 niskie perony :
Perony wyposażone są w oświetlenie , ławki , kosze na śmieci i gabloty z rozkładem jazdy pociągów .
Dostęp do peronów jest możliwy przez jedyne przejście naziemne przy wschodnim krańcu peronu 2 .
Przy peronie 1 znajdował się budynek , który pełni rolę poczekalni , a w przeszłości także kasy biletowej .
Budynek ten pełni także funkcję mieszkaniową .
Stacja jest obsługiwana przez pociągi osobowe spółki Polregio relacji Białystok – Szepietowo – Białystok .
