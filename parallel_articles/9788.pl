Życiorys .
W 1973 ukończył szkołę religijną İmam Hatip Lisesi w Stambule , a w 1981 studia na Wydziale Ekonomii i Handlu Uniwersytetu Marmara .
Pochodzi z ubogiej rodziny pracownika straży przybrzeżnej z Rize .
Wychowywał się w stambulskiej dzielnicy Kasımpaşa .
W młodości zarabiał na życie sprzedając na ulicy lemoniadę i sezamki .
Przez wiele lat amatorsko uprawiał piłkę nożną .
W czasie studiów zaangażował się w działalność polityczną .
Był członkiem Tureckiego Narodowego Związku Studentów .
Związał się z ruchem islamistycznym skupionym wokół Necmettina Erbakana .
Wstąpił do islamistycznej Partii Ocalenia Narodowego ( MSP ) .
W 1976 został wybrany na przewodniczącego organizacji młodzieżowej MSP w Beyoğlu , a następnie został szefem organizacji młodzieżowej MSP w Stambule .
Po przewrocie wojskowym 12 września 1980 , po którym zdelegalizowano wszystkie partie polityczne , Erdoğan pracował w sektorze prywatnym jako doradca i menedżer .
W 1983 wrócił do działalności politycznej jako członek Partii Dobrobytu .
Rok później został przewodniczącym okręgowej organizacji partyjnej w Beyoğlu , a w 1985 przewodniczącym organizacji regionalnej w Stambule oraz członkiem centralnych władz partii .
27 marca 1994 wygrał wybory na stanowisko burmistrza Stambułu .
W okresie sprawowania tej funkcji ( 1994-1998 ) , Erdoğanowi udało się rozwiązać szereg problemów w sferze gospodarki , infrastruktury , komunikacji oraz ekologii , m.in .
