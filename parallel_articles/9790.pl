Recife
Recife ( wym .
[ ] ) – miasto we wschodniej Brazylii , liczące razem z przedmieściami 3,4 mln mieszkańców .
Historia .
Port nad Atlantykiem założony przez Portugalczyków w 1537 roku w pobliżu Olindy .
Pozostawał w ich posiadaniu do uzyskania przez Brazylię niepodległości , z wyjątkiem okresu 1630–1654 , kiedy był stolicą Nowej Holandii .
Słynął z plantacji trzciny cukrowej .
W 1817 stolica rządu republikańskiego podczas krótkotrwałego powstania , stłumionego przez siły monarchii portugalskiej .
Współczesność .
Recife jest dużym centrum gospodarczym i turystycznym .
Posiada katedrę z XVII wieku , barokowe kościoły , fort i pałac gubernatorski z XVIII wieku .
Połączenia lotnicze zapewnia Port lotniczy Recife .
W mieście swoje siedziby ma kilka klubów piłkarskich , m.in .
Sport Recife , Náutico Recife , Santa Cruz Recife , Manchete Recife .
Gospodarka .
W mieście rozwinął się przemysł spożywczy , włókienniczy , odzieżowy oraz cementowy .
