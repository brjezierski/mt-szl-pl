Reda ( rzeka )
Reda – rzeka pobrzeża Bałtyku położona w północnej Polsce w województwie pomorskim .
Nad rzeką leżą Wejherowo i Reda .
Uchodzi do Zatoki Puckiej .
Ma długość 51 km .
Średni spadek doliny rzecznej wynosi 0,98‰ .
Średni przepływ wody przy ujściu obliczono na 5,5 m³/s .
Dorzecze Redy .
Dorzecze rzeki Redy obejmuje obszar 485 km² .
