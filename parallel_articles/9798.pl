Redcar
Redcar – miasto w północno-wschodniej Anglii , w regionie North East England , w hrabstwie ceremonialnym North Yorkshire , na terenie jednolitej jednostki administracyjnej ( `` unitary authority '' ) Redcar and Cleveland , położone nad Morzem Północnym .
W 2001 miasto to zamieszkiwało 36 610 osób .
Mieści się w nim siedziba klubu żużlowego – Redcar Bears .
