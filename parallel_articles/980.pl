Agata Mróz-Olszewska
Agata Danuta Mróz-Olszewska ( ur .
7 kwietnia 1982 w Dąbrowie Tarnowskiej , zm .
4 czerwca 2008 we Wrocławiu ) – polska siatkarka , 138-krotna reprezentantka Polski w latach 2003–2006 , zdobywczyni złotych medali na Mistrzostwach Europy w piłce siatkowej kobiet w 2003 i 2005 .
Życiorys .
Życie prywatne .
Agata Mróz urodziła się w Dąbrowie Tarnowskiej , jednak dzieciństwo i młodość spędziła w Tarnowie , mieszkała przy ul.Grottgera , gdzie rozpoczęła trenowanie siatkówki .
Pochodziła ze sportowej rodziny .
Oboje rodzice Ryszard i Lucyna uprawiali w młodości sport , jej rodzeństwo robi to nadal .
Siostra Katarzyna jest była zawodową siatkarką , a brat Paweł – zawodowym koszykarzem .
W grudniu 2005 Mróz wzięła udział w sesji zdjęciowej do magazynu CKM .
9 czerwca 2007 wyszła za mąż za Jacka Olszewskiego , którego poznała w Szczyrku .
12 stycznia 2008 roku ogłosiła , że spodziewa się dziecka .
Lekarze doradzali jej aby usunęła ciążę , na co się nie zdecydowała .
4 kwietnia 2008 urodziła przez cesarskie cięcie córkę Lilianę , która jest wcześniakiem .
22 maja 2008 przeszła zabieg przeszczepienia szpiku kostnego w Klinice Transplantacji Szpiku , Onkologii i Hematologii Dziecięcej Akademii Medycznej we Wrocławiu .
Czternaście dni po przeszczepie zmarła w wyniku posocznicy i związanego z nią wstrząsu septycznego .
Rodzina z mediów dowiedziała się o jej śmierci .
Agata Mróz miała 26 lat .
Została pochowana 9 czerwca 2008 ( w 1. rocznicę ślubu ) na cmentarzu w Tarnowie-Krzyżu w północnej części miasta , została skremowana .
Jej córka gra w siatkówkę w klubie KS Saska Warszawa .
Kariera .
Jej talent dostrzeżono w młodości. , zaczynała w Tarnovii , jej pierwszą trenerka byla Małgorzata Podstawska , Dlatego jako szkołę średnią w wieku 15 lat , zaproponowano jej sosnowiecką Szkołę Mistrzostwa Sportowego Polskiego Związku Piłki Siatkowej .
W trzeciej klasie liceum ogólnokształcącego przy tejże szkole odniosła swój pierwszy ważny sukces .
Reprezentacja Polski kadetek w Gdańsku wywalczyła złoty medal , a Agata Mróz była wówczas kapitanem tamtej reprezentacji .
Tuż po tym , wiosną 2000 roku , przerwała treningi z powodu pierwszych objawów choroby , która później miała okazać się śmiertelna .
Dokończyła trzecią klasę w LO przy SMS i powróciła do Tarnowa .
Szkołę średnią ukończyła w Liceum Ogólnokształcące im .
Kazimierza Brodzińskiego w Tarnowie , Po dłuższej przerwie rozpoczęła treningi za namową ojca w drużynie serii B kobiecych rozgrywek siatkarskich w Polsce , akademickiej drużynie z Ostrowca Świętokrzyskiego .
Stąd dostała pierwsze powołanie do kadry seniorskiej , co wywołało spore poruszenie , gdyż – mimo sukcesów juniorskich – Agata Mróz była niemal nieznaną zawodniczką , podobnie jak jej drużyna AZS WSBiP Ostrowiec Świętokrzyski .
Postawa w reprezentacji , w której stanowiła jeden z filarów podczas zwycięskich Mistrzostw Europy ( 2003 ) , sprawiła , że nieznaną dotąd zawodniczką zainteresowało się wiele czołowych klubów , nie tylko polskich .
Agata Mróz zdecydowała się przyjąć propozycję BKS Stal Bielsko-Biała , z którą to drużyną w 2004 roku wywalczyła mistrzostwo Polski , a w latach 2004 i 2006 roku – Puchar Polski .
Była wielokrotną reprezentantką Polski w siatkówce .
W 2005 roku ponownie wywalczyła złoty medal Mistrzostw Europy .
Od 2006 r. występowała w hiszpańskim zespole Gruppo Murcia 2002 , z którym w 2007 roku zdobyła m.in .
mistrzostwo i Puchar Hiszpanii .
W 2007 roku , ze względu na stan zdrowia ( mielodysplazja szpiku ) , postanowiła przerwać karierę .
