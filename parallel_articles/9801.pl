Redwood City
Redwood City – miasto w Stanach Zjednoczonych w stanie Kalifornia .
Redwood City jest jednym z przedmieść San Francisco .
Redwood City to miasto gdzie swoją siedzibę mają firmy : Oracle , EA Games ( firma tworząca gry komputerowe ) .
W mieście rozwinął się przemysł skórzany .
Demografia .
W 2000 mieszkało 79 000 osób , było 29 870 gospodarstw domowych i 17 902 rodzin .
Średnia wieku 35 lat .
Na 100 kobiet przypada 100,4 mężczyzn .
Średni dochód gospodarstwa domowego w mieście wynosił $ 72 679 a na rodzinę $ 82 964 .
Średni dochód przypadający na mężczyznę $ 50 345 , na kobietę $ 41 125 .
