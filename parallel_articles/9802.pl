Referendum w Wielkiej Brytanii w 2016 roku
Referendum w Wielkiej Brytanii w 2016 roku – niewiążące referendum dotyczące dalszego członkostwa Wielkiej Brytanii w Unii Europejskiej , które odbyło się 23 czerwca 2016 roku .
Opuszczenie Unii Europejskiej przez Wielką Brytanię określane jest przez media terminem brexit .
Pomimo niewiążącego charakteru referendum , rząd Theresy May uznał jego wynik za przyzwolenie na użycie Artykułu 50 bez zgody Parlamentu .
Wątpliwość tę rozstrzygnął High Court of Justice 3 listopada 2016 .
