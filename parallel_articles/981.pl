Agata Sycylijska
Agata Sycylijska , św .
Agata , Agata z Katanii ( ; ur .
235 , zm .
5 lutego 251 ) – dziewica i męczennica chrześcijańska , święta Kościoła katolickiego i prawosławnego .
Żywot .
Zgodnie z opisem męczeństwa pochodzącym z V wieku , Agata pochodziła ze znamienitego rodu rzymskiego .
Urodziła się w Katanii na Sycylii .
Po przyjęciu wiary chrześcijańskiej postanowiła żyć w dziewictwie .
Gdy odrzuciła rękę namiestnika Sycylii Kwincjana , oddano ją do domu rozpusty .
Mimo to zachowała dziewictwo i nie wyparła się Chrystusa .
Kiedy cesarz Decjusz rozpoczął prześladowania chrześcijan , Kwincjan jako jedną z pierwszych postawił przed sądem Agatę .
Skazano ją na tortury , podczas których odcięto jej piersi .
Z nastaniem niespodziewanego trzęsienia ziemi namiestnik nakazał zaprzestania tortur , dostrzegając w tym karę Bożą .
Ostatecznie Agata poniosła śmierć rzucona na rozżarzone węgle .
Jej ciało zostało pogrzebane przez chrześcijan poza miastem .
Kult świętej .
W rok po jej śmierci nastąpił wielki wybuch Etny .
Spływająca lawa nie zalała jednak miasta , a zatrzymała się tuż przed nim .
.
Wspomnienie liturgiczne Agaty męczennicy w Kościele katolickim obchodzone jest 5 lutego natomiast Kościół prawosławny wspomina ją 5/18 lutego , tj .
18 lutego według kalendarza gregoriańskiego .
W tym dniu błogosławi się chleb , sól , wodę , które mają zapobiegać pożarom , szczególnie od pioruna .
Jej znajdujące się w Katanii relikwie umieszczono w relikwiarzu , który w każdą rocznicę jej śmierci obwożony jest po mieście w uroczystej procesji .
Jej wizerunek znajduje się na Orderze Świętej Agaty , a dzień 5 lutego jest świętem państwowym w San Marino .
Jest świętą , do której chrześcijanie zwracają się przy zagrożeniach związanych z ogniem i pożarami ; ma również chronić Sycylię przed wybuchami Etny .
Patronuje przede wszystkim zawodom związanym z ogniem : kominiarzom , ludwisarzom , odlewnikom , a także z powodu męczeńskiej śmierci – pielęgniarkom .
Jest również orędowniczką w chorobach piersi .
W ikonografii chrześcijańskiej św .
Agata przedstawiana jest w długiej sukni , z kleszczami , którymi zadawano jej tortury .
Atrybutami Agaty są : dom w płomieniach , kość słoniowa – symbol czystości i niewinności oraz siły moralnej – a także misa z obciętymi podczas męczeństwa piersiami .
