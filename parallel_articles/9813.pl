Reims
Reims ( wym .
[ ] , uproszczona : `` rę : s '' ) – miasto i gmina w północno-wschodniej części Francji w regionie administracyjnym Grand Est , w departamencie Marna .
Położone w odległości ok. 160 km od Paryża nad rzeką Vesle .
W 1990 r. gęstość zaludnienia wynosiła 3851 osób/km² .
Liczba mieszkańców 180 620 w 1990 , 180 000 w 2002 .
