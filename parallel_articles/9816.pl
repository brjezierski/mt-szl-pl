Reit im Winkl
Reit im Winkl – miejscowość i gmina w południowo-wschodnich Niemczech , w kraju związkowym Bawaria , w rejencji Górna Bawaria , w regionie Südostoberbayern , w powiecie Traunstein .
Leży około 25 km na południowy zachód od Traunsteinu , przy granicy z Austrią , przy drodze B305 .
Centrum sportów zimowych.1991 roku odbyły się tu mistrzostwa świata juniorów w narciarstwie klasycznym .
W latach 1987,1989,1990 , oraz 2000-2002 i 2004 roku organizowano tu Puchary Świata w kombinacji norweskiej .
Dzielnice .
Dzielnicami w gminie są : Birnbach , Blindau , Entfelden , Groißenbach , Maserer-Pass , Reit im Winkl , Seegatterl i Winklmoos .
Polityka .
Wójtem gminy jest Josef Heigenhauser , wcześniej urząd ten obejmował Fritz Schmuck , rada gminy składa się z 14 osób .
