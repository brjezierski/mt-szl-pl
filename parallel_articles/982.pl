Agatha Christie
Agatha Christie , lady Mallowan , DBE , właśc .
Agatha Mary Clarissa Miller Christie ( ur .
15 września 1890 w Torquay , zm .
12 stycznia 1976 w Wallingford ) – brytyjska autorka powieści kryminalnych i obyczajowych .
Najbardziej znana na świecie pisarka kryminałów oraz najlepiej sprzedająca się autorka wszech czasów .
Wydano ponad miliard egzemplarzy jej książek w języku angielskim oraz drugi miliard przetłumaczonych na 45 języków obcych .
Pod pseudonimem Mary Westmacott wydała kilka powieści obyczajowych , które również cieszyły się popularnością .
Christie wydała ponad 90 powieści i sztuk teatralnych .
Ich akcja toczyła się głównie w zamkniętych pomieszczeniach , a mordercą mógł być tylko jeden z mieszkańców .
Wniosła wiele nowych rozwiązań do tradycyjnej sztuki pisania kryminałów .
Niektóre z jej powieści mają zaskakujące i wcześniej niespotykane w kryminałach rozwiązania , np .
`` Zabójstwo Rogera Ackroyda '' czy `` Morderstwo w Orient Expressie '' .
Jej sztuka `` Pułapka na myszy '' , wystawiona po raz pierwszy w 1952 roku w Londynie , osiągnęła liczbę ponad dwudziestu tysięcy przedstawień .
Wiele z jej powieści i opowiadań zostało zekranizowanych , niektóre wielokrotnie ( `` Morderstwo w Orient Expressie '' , `` Śmierć na Nilu '' czy `` 4.50 z Paddington '' ) .
Na podstawie dzieł autorki powstały także seriale telewizyjne i słuchowiska radiowe .
Wszystkie utwory objęte były w 1951 roku w Polsce zapisem cenzury , podlegając natychmiastowemu wycofaniu z bibliotek .
Życiorys .
Urodziła się 15 września 1890 w nadmorskim Torquay w południowo-zachodniej Anglii w hrabstwie Devon , gdzie mieściła się Ashfield , posiadłość jej matki .
Była trzecim dzieckiem Clary i Fryderyka Millerów , miała siostrę Margaret i brata Louisa .
Decyzją matki , uczyła się w domu .
W 1901 zmarł jej ojciec .
12 października 1912 podczas balu w rodzinnym Torquay poznała starszego od siebie o rok pułkownika Archibalda Christie .
W wigilię 1914 wzięła z nim ślub .
Mieli córkę , Rosalind Hicks ( ur .
5 sierpnia 1919 , zm .
28 października 2004 ) .
Podczas I wojny światowej pracowała jako pielęgniarka w szpitalu , asystentka w aptece oraz jako technik farmaceutyczny , dzięki czemu poznała właściwości trucizn , co wykorzystywała później w swoich powieściach .
W 1926 zmarła jej matka , brat uzależnił się od narkotyków , wydawcy nie spodobała się powieść „ Zabójstwo Rogera Ackroyda ” , a jej mąż zakochał się w innej kobiecie ( Nancy Neele ) i zażądał rozwodu .
W grudniu 1926 zaginęła na 11 dni , co wywołało poruszenie w prasie ( m. in .
`` The New York Times , Daily Herald '' ) .
Przebywała wówczas w Hydropathic Hotel w uzdrowiskowym Harrogate w Yorkshire , gdzie zameldowała się jako Teresa Neele .
W tym czasie popadła w amnezję .
W 1928 rozwiodła się z mężem .
Wskutek problemów w życiu osobistym skupiła się na pisaniu kolejnych książek i podróżowaniu po świecie .
W 1930 wyszła za młodszego od niej o 13 lat archeologa Maxa Mallowana , którego poznała podczas jednego z wyjazdów w rejon Zatoki Perskiej .
Wyjeżdżała z nim na wykopaliska na Bliskim Wschodzie , który później stał się tłem kilku jej powieści ( m.in .
`` Śmierci na Nilu '' i `` Morderstwie w Mezopotamii '' ) .
Akcja innych dzieł autorki rozgrywa się w Torquay , miejscu jej narodzin .
