Remagen
Remagen – miasto w Niemczech w kraju związkowym Nadrenia-Palatynat w powiecie Ahrweiler .
Leży na południe od Bonn .
Pod koniec II wojny światowej znajdował się tu jedyny niezniszczony most na Renie .
W Remagen znajduje się oddział wydziału ekonomii i zarządzania Politechniki Koblenckiej `` ( Fachhochschule Koblenz ) '' , tzw .
`` RheinAhrCampus '' .
