Rembrandt
Rembrandt Harmenszoon van Rĳn ( ur .
15 lipca 1606 w Lejdzie , zm .
4 października 1669 w Amsterdamie ) – holenderski malarz , rysownik i grafik .
Uważany powszechnie za jednego z największych artystów europejskich i światowych .
Obok Jana Vermeera i Fransa Halsa najważniejszy malarz w sztuce holenderskiej „ złotego wieku ” – okresu , w którym kultura , nauka , gospodarka , potęga militarna i wpływy polityczne Holandii osiągnęły apogeum .
W okresie lejdejskim swoje obrazy sygnował skrótem „ RHL ” ( Rembrandus Hermanii Leidensis ) .
Później podpisywał się jedynie samym imieniem , podobnie jak wielcy artyści renesansu : Michał Anioł , Rafael lub Tycjan .
Życiorys .
Był ósmym ( przedostatnim ) dzieckiem młynarza Harmena Gerritszoona oraz córki piekarza Neeltge Willemsdochter van Zuitbroeck .
Dzieciństwo spędził w Lejdzie .
Od siódmego roku życia uczył się w szkole łacińskiej .
Siedem lat później , 20 maja 1620 r. , wstąpił na uniwersytet w Lejdzie , ale studiował tam tylko kilka miesięcy .
W 1621 r. rozpoczął naukę malarstwa w pracowni Jacoba van Swanenburgha , w której przez 3 lata uczył się podstaw i techniki malarstwa .
W 1624 r. rodzice wysłali go do Amsterdamu , gdzie doskonalił swój kunszt malarski u Pietera Lastmana ( 1583–1633 ) , uznanego malarza obrazów historycznych , cechujących się silnym realizmem i wielkim napięciem dramatycznym .
Od niego zapożyczył styl narracyjny , sposób ukazywania psychologicznej głębi postaci oraz technikę światłocienia .
Po powrocie do Lejdy otworzył własną pracownię i rozpoczął współpracę z osiemnastoletnim Janem Lievensem ( 1607–1674 ) , również uczniem Lastmana .
Około 1628 r. pracownię młodych artystów odwiedził Constantijn Huygens , dyplomata , podróżnik i poeta .
Było to przełomowe wydarzenie w karierze Rembrandta i Jana Lievensa .
Constantĳn Huygens był jedną z najbardziej wpływowych osobistości w Holandii , a tym samym jego poparcie , jak również osąd w dziedzinie sztuki , miały decydujące znaczenie .
Huygens zamówił portret u Jana Lievensa , natomiast Rembrandt miał namalować portret jego brata .
Przez kilka następnych lat Huygens śledził ich karierę , organizował zamówienia , pełnił rolę kogoś w rodzaju agenta .
W 1630 r. umarł ojciec malarza .
W 1631 r. Rembrandt osiadł w Amsterdamie , gdzie marszand obrazów Hendrick van Uylenburgh ( ok. 1587–1661 , wiele lat mieszkający w Krakowie , później w Gdańsku ) zaproponował mu otwarcie u siebie pracowni .
Za jego pośrednictwem Rembrandt otrzymał prestiżowe zamówienia .
Malował głównie portrety zamawiane przez najbogatsze rodziny Amsterdamu .
5 czerwca 1633 r. zaręczył się z Saskią van Uylenburgh , bliską krewną Hendricka ( kuzynką lub bratanicą/siostrzenicą ) , którą poślubił 22 czerwca 1634 r. Żona , córka burmistrza , wniosła posag w wysokości 40 tys .
florenów .
Młodzi małżonkowie zamieszkali w luksusowym domu w dzielnicy nad rzeką Amstel .
Artysta często uwieczniał żonę na swoich płótnach .
W 1635 r. Rembrandt otworzył pracownię w wynajętym magazynie , przez którą przewinęło się następnie kilkudziesięciu uczniów .
W styczniu 1639 r. kupił duży dwupiętrowy dom przy Jodenbreestraat , w dzielnicy żydowskiej .
W 1640 r. umarła matka Rembrandta .
