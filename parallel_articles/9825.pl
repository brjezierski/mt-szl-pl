Geografia .
Miasto leży nad rzeką Mozela oraz na północ od miasta Épinal .
Historia .
Nazwa miasta wzięła się od imienia Św .
Romarica , jednego z towarzyszy Kolumbana Młodszego .
Romaric wybudował na pobliskich wzgórzach klasztor , który obecnie wznosi się nad miastem .
W 1638 roku miasto zostało zaatakowane przez Francuzów , a w 1682 zniszczone przez trzęsienie ziemi .
W 1766 roku miasto zostało , wraz z całą Lotaryngią , włączone w granice Francji .
