( 575 ) Renate
( 575 ) Renate – planetoida z pasa głównego asteroid okrążająca Słońce w ciągu 4 lat i 33 dni w średniej odległości 2,56 j.a .
Została odkryta 19 września 1905 roku w Landessternwarte Heidelberg-Königstuhl w Heidelbergu przez Maxa Wolfa .
Nazwa planetoidy pochodzi od imienia `` Renate '' ( została zainspirowana literami oznaczenia asteroidy [ 1905 RE ] w imieniu REnate ) .
Przed jej nadaniem planetoida nosiła oznaczenie tymczasowe ( 575 ) 1905 RE .
