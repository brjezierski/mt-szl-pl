Reni ( ukr .
`` Рені '' , rum .
`` Reni '' ) – miasto na Ukrainie w obwodzie odeskim , liczy 21 tys .
mieszkańców ( 2006 ) .
Ośrodek przemysłu spożywczego , ważny port nad Dunajem .
Reni jest stolicą rejonu o tej samej nazwie , który liczy ok. 41 tys .
mieszkańców .
Znaczną część ludności stanowią Mołdawianie – według rumuńskich i ukraińskich źródeł stanowią oni prawie 50 % mieszkańców rejonu i miasta .
Przez Reni przechodzi linia kolejowa łącząca rumuńskie miasto Gałacz z Mołdawią ( m.in .
przez Gagauzję ) .
Z dworca kolejowego w Reni nie można zatem dojechać do żadnej innej miejscowości na Ukrainie , nie przekraczając granicy mołdawskiej .
Brak możliwości dogodnego transportu towarów koleją do innych miast na Ukrainie zadecydował o upadku znaczenia portu w Reni na rzecz m.in .
Odessy i Izmaiłu .
