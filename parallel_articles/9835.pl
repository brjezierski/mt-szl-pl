Reprezentacja Śląska w piłce nożnej mężczyzn
Reprezentacja Śląska w piłce nożnej – nieformalna drużyna piłkarska , w której skład wchodzą wyłącznie zawodnicy grający na co dzień w górnośląskich klubach piłkarskich .
Nie jest zrzeszona w FIFA oraz UEFA , w związku z czym nie posiada prawa do udziału w oficjalnych turniejach międzynarodowych , a także nie rozgrywa oficjalnych meczów międzypaństwowych .
Barwy drużyny to żółto-niebieskie .
Historia .
Historia piłkarskiej reprezentacji Górnego Śląska sięga początku lat międzywojennych .
Pierwszy mecz miał miejsce 18 czerwca 1920 roku w Lipinach – w ramach propagowania polskości na ziemiach śląskich – został rozegrany pierwszy mecz zawodników wywodzących się z tego regionu przeciwko Czarnym Lwów – czołowemu wówczas polskiemu klubowi piłkarskiemu , którzy wygrali mecz aż 8:3 .
W 1933 roku powstała liga Śląska , której zwycięzca otrzymywał tytuł mistrza Śląska i brał udział w eliminacjach do Ligi Państwowej .
