Reprezentacja Niemiec w piłce nożnej mężczyzn
Reprezentacja Niemiec w piłce nożnej mężczyzn ( niem .
`` Die deutsche Fußballnationalmannschaft der Männer '' ) – narodowy zespół piłkarzy nożnych , który reprezentuje Republikę Federalną Niemiec w meczach i turniejach międzynarodowych .
Za istnienie reprezentacji odpowiedzialny jest .
Reprezentacja Niemiec to obok Brazylii najbardziej utytułowana drużyna narodowa .
Wystąpiła w 20 turniejach finałowych MŚ ( 1934 , 1938 , 1954 , 1958 , 1962 , 1966 , 1970 , 1974 , 1978 , 1982 , 1986 , 1990 , 1994 , 1998 , 2002 , 2006 , 2010 , 2014 , 2018 , 2022 ) .
Ustępuje jedynie reprezentacji Brazylii , która wystąpiła we wszystkich turniejach .
Zdobyła czterokrotnie Puchar Świata ( 1954 , 1974 , 1990 i 2014 ) .
Wystąpiła 12 razy w turniejach finałowych ME ( 1972 , 1976 , 1980 , 1984 , 1988 , 1992 , 1996 , 2000 , 2004 , 2008 , 2012 , 2016 ) .
Zawodnicy Niemiec zdobyli mistrzostwo Europy trzy razy ( 1972 , 1980 i 1996 ) .
Mają najlepsze statystyki medalowe z wszystkich reprezentacji na Mistrzostwach Europy , a na świecie ustępują jedynie Brazylii .
Na podstawie przepisów RFN oraz międzynarodowego uznania uznaje się współczesną reprezentację Niemiec za sukcesora reprezentacji Cesarstwa Niemieckiego , Republiki Weimarskiej i III Rzeszy Niemieckiej .
