Reprezentacja Anglii w piłce nożnej mężczyzn
Reprezentacja Anglii w piłce nożnej – narodowy zespół piłkarzy nożnych Anglii , który obok reprezentacji Szkocji jest najstarszą drużyną narodową w piłce nożnej .
Początki .
Angielska reprezentacja narodowa zagrała swój pierwszy mecz 5 marca 1870 na Kennington Oval .
Rywalem była Szkocja .
Meczu tego nie uznaje się jednak za oficjalny , ponieważ zespół przeciwników nie był oficjalną reprezentacją swego kraju , a drużyną złożoną ze Szkotów mieszkających w Londynie lub jego okolicach .
Mecz ten zakończył się remisem 1:1 .
Później Anglicy grali jeszcze cztery spotkania z reprezentacją londyńskich Szkotów , trzy razy wygrywając i raz remisując .
Po jakimś czasie Football Association zdecydował się zorganizować w pełni profesjonalny mecz .
Rywalem znów była Szkocja , tym razem złożona ze Szkotów mieszkających w swoim kraju .
Sędzią również był Szkot – William Keay , a samo spotkanie odbyło się w Glasgow .
Anglicy wyszli na mecz w ustawieniu 2-1-7 , Szkoci – 2-2-6 , jednak mimo ofensywnej taktyki mecz ten zakończył się bezbramkowym remisem .
