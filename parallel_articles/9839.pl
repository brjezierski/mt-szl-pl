Repty Śląskie
Repty Śląskie – dzielnica Tarnowskich Gór , utworzona w 1998 roku .
Obejmuje obszar miejscowości Repty ( składającej się ze Starych i Nowych Rept ' ) , włączonej w granice miasta Tarnowskie Góry w 1973 roku .
Nazwa .
Toponomastycy nazwę `` Repty '' wywodzą od staropolskiego czasownika `` reptać '' oznaczającego ‘ szemrać ’ , ‘ szumieć ’ , sugerując związek z przepływającą przez te tereny rzeką Dramą .
Istnieje jednak również hipoteza głosząca pochodzenie nazwy `` Rept '' od właścicieli – `` Reptów '' .
Wówczas byłaby to nazwa rodowa .
Johann Knie w wykazie miejscowości Śląska z 1830 roku podaje równocześnie polskie i niemieckie formy nazw obu miejscowości : wsi `` Stare Repty '' – `` Alt-Repten '' oraz jej ówczesnej kolonii `` Nowe Repty '' – `` Neu-Repten '' .
