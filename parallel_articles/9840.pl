Republika Ałtaju
Republika Ałtaju ( ) – autonomiczna republika , wchodząca w skład Federacji Rosyjskiej , leżąca w południowej części zachodniej Syberii , w zlewisku rzek Bija i Katuń .
W ramach federacji Ałtaj graniczy z Tuwą , Chakasją , Krajem Ałtajskim oraz obwodem kemerowskim , ma także zewnętrzną granicę z Chinami , Mongolią i Kazachstanem .
Geografia .
Klimat umiarkowany kontynentalny , z krótkim , gorącym latem i długą , mroźną zimą .
Powierzchnia republiki zróżnicowana , charakterystyczne wysokie grzbiety gór Ałtaj , poprzecinane głębokimi dolinami rzecznymi , rzadko szerokimi kotlinami górskimi .
Najwyższa góra republiki – Biełucha ( ałtajski : Kadyn-Baży ) , której wysokość wynosi 4506 m jest jednocześnie najwyższym punktem Syberii .
Strefa czasowa .
Republika należy do omskiej strefy czasowej ( OMST ) .
UTC +7:00 przez cały rok .
Wcześniej , przed 27 marca 2011 roku , obowiązywał czas standardowy ( zimowy ) strefy UTC+6:00 , a czas letni – UTC+7:00 .
