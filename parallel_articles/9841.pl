Republika Jukatanu
Republika Jukatanu – historyczne państwo na Półwyspie Jukatan , powstałe w wyniku secesji meksykańskiego stanu Jukatan .
Po centralizacji władzy w Meksyku w 1835 r. w kraju doszło do zamieszek w dotychczas autonomicznych terytoriach .
W 1838 r. w Tizimín rebelianci ogłosili niepodległość Jukatanu .
W 1840 r. zebrał się lokalny kongres , który uchwalił odłączenie się od Meksyku .
W 1841 r. prezydent Meksyku Antonio López de Santa Anna wysłał emisariusza do Jukatanu , w celu porozumienia w sprawie powrotu Jukatanu pod władzę Meksyku .
Santa Anna nie zgodził się jednak na ustępstwa w sprawie autonomii , wskutek czego Republika Jukatanu uchwaliła własną konstytucję .
W 1843 r. Santa Anna ogłosił blokadę morską Jukatanu , a następnie wysłał wojska na półwysep .
Ostatecznie zawarto porozumienie , na mocy którego Jukatan powrócił w skład Meksyku na prawach samorządnej prowincji .
W 1845 r. rząd meksykański zniósł autonomię stanu , wobec czego 1 stycznia 1846 r. Jukatan ponownie ogłosił niepodległość .
W 1847 r. doszło także do rewolty Majów przeciwko ludności latynoskiej zamieszkującej półwysep .
W 1848 sytuacja była na tyle dramatyczna , że gubernator republiki wysłał prośby o pomoc do USA , Hiszpanii i Wielkiej Brytanii oferując przekazanie kraju pod rządy tego z państw , które zdusi rewoltę Majów .
Ostatecznie zwrócono się o pomoc do rządu meksykańskiego .
17 sierpnia 1848 r. Republika Jukatanu powróciła w skład Meksyku .
Rewoltę Majów stłumiono dopiero w 1901 r. , choć niektóre grupy utrzymały się do 1910 r .
