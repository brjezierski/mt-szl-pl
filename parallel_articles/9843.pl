Południowa Afryka
Południowa Afryka ( , ) , oficjalnie Republika Południowej Afryki , RPA ( , ) – państwo położone na południowym krańcu Afryki , u styku Oceanu Atlantyckiego i Indyjskiego .
Zajmuje powierzchnię 1 219 090 km² i liczy 56 978 635 mieszkańców ( 2021 ) .
Państwo jest republiką parlamentarną .
Funkcję stolicy pełnią trzy miasta : Pretoria ( siedziba władzy wykonawczej ) , Kapsztad ( siedziba władzy ustawodawczej ) i Bloemfontein ( siedziba władzy sądowniczej ) .
Największym miastem jest Johannesburg .
Walutą jest rand .
Południowa Afryka jest krajem wieloetnicznym i wielokulturowym .
Około 80 % społeczeństwa stanowią czarnoskórzy , 9 % koloredzi a 8 % biali .
Jedenaście języków ma status języka urzędowego ( afrikaans , angielski , xhosa , ndebele , pedi , soto , suazi , tsonga , tswana , venda , zulu ) .
Znaczna część społeczeństwa jest wielojęzyczna .
Język angielski używany jest na szeroką skalę w administracji publicznej , mediach i świecie biznesu .
Południowa Afryka jest trzecią co do wielkości gospodarką afrykańską ( po Egipcie i Nigerii ; PKB realny w 2019 roku – 731 mld USD ) i klasyfikowana jest jako rynek wschodzący .
Gospodarka w dużym stopniu opiera się na sektorze usługowym , choć istotną rolę odgrywa też przemysł , bazujący na bogatych zasobach naturalnych .
Do głównych wyzwań , z którymi zmaga się państwo należą wysoki poziom nierówności społecznych , bezrobocia , ubóstwa , przestępczości i korupcji , jak i epidemia HIV/AIDS .
Obszar ten zamieszkany jest od czasów prehistorycznych .
Osiedli tu Buszmeni , Khoikhoi , a w późniejszym okresie także ludy Bantu .
W 1652 roku Holendrzy założyli kolonię nad Przylądkiem Dobrej Nadziei , która była przystankiem na trasie żeglugowej z Europy do Azji .
W 1806 roku terytorium to przeszło w posiadanie brytyjskie ( Kolonia Przylądkowa ) .
Niezadowolenie z polityki prowadzonej przez brytyjską administrację kolonialną , w tym zniesienie niewolnictwa , skłoniło część osadników holenderskich ( Burów ) do migracji w głąb kontynentu ( Wielki Trek ) , gdzie założyli oni szereg republik burskich .
W 1902 roku , po serii konfliktów zbrojnych ( wojna brytyjsko-zuluska , wojny burskie ) , motywowanych m.in .
odkryciem na tym obszarze bogatych złóż złota i diamentów , cały ten obszar znalazł się w rękach brytyjskich .
W 1910 roku utworzony został Związek Południowej Afryki , mający status dominium brytyjskiego , który pełną suwerenność uzyskał w 1934 roku .
W 1961 roku państwo proklamowane zostało republiką , kończąc jednocześnie zwierzchność monarchy brytyjskiego ( tzw .
`` Commonwealth realm '' ) .
Władza w kraju od początku jego istnienia skupiona była w rękach białej mniejszości .
Od 1948 roku rząd prowadził politykę apartheidu ( segregacji rasowej ) .
Protesty wewnętrzne oraz rosnąca presja międzynarodowa doprowadziły do jej zakończenia na początku lat 90 .
XX wieku .
W 1994 roku przeprowadzone zostały pierwsze wybory powszechne , w wyniku których prezydentem został Nelson Mandela , a władzę objął Afrykański Kongres Narodowy .
