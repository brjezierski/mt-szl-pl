Republika Rio Grande
Republika Rio Grande – krótko istniejące państwo w Ameryce Północnej pomiędzy Republiką Teksasu a Meksykiem .
17 stycznia 1840 położone przy granicy z Republiką Teksasu meksykańskie stany Coahuila , Nuevo León i Tamaulipas ogłosiły secesję i utworzenie nowego państwa ze stolicą w Laredo .
Tego samego dnia powołano władze republiki , na których czele jako prezydent stanął Jesús de Cárdenas .
W dniach 24-25 marca 1840 pod Morales doszło do bitwy między wojskami republiki a armią meksykańską , w której armia Rio Grande poniosła klęskę .
Wojska republiki wycofały się do Teksasu , skąd powróciły 1 czerwca zasilone niewielką grupą teksańskich ochotników .
25 października oddziały Rio Grande ponownie poniosły klęskę .
6 listopada dowódca armii Rio Grande , Antonio Canales Rosillo zawarł porozumienie z rządem meksykańskim , na mocy którego rozpuścił swoje oddziały .
Pozbawiona wojska republika przestała tym samym istnieć .
