Republika Teksasu
Republika Teksasu ( ; ) – państwo w Ameryce Północnej , istniejące w latach 1836–1845 .
Historia .
Teksas uzyskał niepodległość 2 marca 1836 roku w wyniku rewolucji teksańskiej ( bitwa o Alamo ) .
W jego granicach znajdowały się wówczas części terytoriów Nowego Meksyku , Oklahomy , Kansas , Kolorado i Wyoming .
Łączna powierzchnia wynosiła 1 007 935 km² , zaś liczba ludności około 70 tysięcy ( dane z roku 1840 ) .
Pierwszy tymczasowy rząd Republiki Teksasu powstał .
Uchwalił on powołanie stanowiska gubernatora ( został nim Sam Houston ) oraz stworzył kilka urzędów , które stały się zalążkami przyszłych ministerstw i departamentów .
Deklaracja niepodległości Teksasu została uchwalona dnia 20 grudnia 1835 roku w miejscowości Goliad i została ona zaakceptowana zarówno przez mieszkających tam Tejanos ( pierwotnych mieszkańców tych terenów ) jak i przez nowo przybyłych osadników .
Państwo zakończyło żywot 29 grudnia 1845 roku , w chwili przystąpienia do USA jako 28. stan .
Stało się to bezpośrednią przyczyną wybuchu wojny amerykańsko–meksykańskiej w kwietniu 1846 roku .
