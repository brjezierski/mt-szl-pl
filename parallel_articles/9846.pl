Republika ( indonezyjski dziennik )
Republika – indonezyjski dziennik .
Właścicielem pisma jest przedsiębiorstwo Mahaka Media .
Jego pierwszy numer ukazał się w 1993 roku .
W wersji drukowanej ukazywał się do końca 2022 roku .
W 1995 roku uruchomiono pokrewny serwis internetowy – Republika Online ( republika.co.id ) .
Portal jest dostępny także w wersji anglojęzycznej ( pod domeną en.republika.co.id ) .
W maju 2018 r. portal Republika.co.id był 53. stroną WWW w kraju pod względem popularności ( według danych Alexa Internet ) .
