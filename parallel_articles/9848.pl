Republika Zielonego Przylądka
Republika Zielonego Przylądka ( , ) – państwo wyspiarskie na Oceanie Atlantyckim , położone ponad 450 km na zachód od zachodnich wybrzeży Afryki , geograficznie tożsame z Wyspami Zielonego Przylądka .
Obecnie członek Unii Afrykańskiej i ECOWAS .
Pochodzenie nazwy .
Nazwa państwa pochodzi od znajdującego się na wschód od wysp Przylądka Zielonego .
Geografia .
Wyspy Zielonego Przylądka stanowią archipelag położony w środkowej części Oceanu Atlantyckiego , ponad 450 km na zachód od wybrzeży Afryki , na wysokości Przylądka Zielonego .
Archipelag ten składa się z 10 głównych wysp i 16 mniejszych wysepek .
Dzielą się one na dwie grupy : Wyspy Zawietrzne ( `` Ilhas de Barlavento '' ) i Podwietrzne ( `` Ilhas de Sotavento '' ) .
Wyspy Zawietrzne to położone na północy wyspy : Santo Antão , São Vicente , Santa Luzia , São Nicolau , Sal i Boa Vista .
Wyspy Podwietrzne leżą na południu , są to Maio , Santiago , Fogo i Brava .
Wnętrza wysp są górzyste z najwyższym szczytem Pico do Fogo – 2829 m n.p.m. , będącym aktywnym wulkanem ( ostatnia erupcja w 2014 r. ) .
Wyspy zajmują powierzchnię 4033 km² .
