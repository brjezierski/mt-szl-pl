Republiki Federacji Rosyjskiej
Republiki wchodzące w skład Federacji Rosyjskiej .
Zgodnie z artykułem 65 rozdziału 3 .
Konstytucji Federacji Rosyjskiej , w skład Rosji wchodzi 21 republik :
Nie wliczając Republiki Krymu , powyższe republiki stanowią ok. 28,6 % powierzchni Federacji Rosyjskiej .
Każda republika posiada własną konstytucję .
Nieutworzone republiki .
W latach 90 .
XX wieku podejmowano próby podniesienia niektórych jednostek administracyjnych Rosji do rangi republiki , jak np .
Kraj Nadmorski , Petersburska Republika Autonomiczna lub proklamowana 13 listopada 1990 roku Nieniecka Autonomiczna Socjalistyczna Republika Radziecka , jednak nie osięgnięto tego celu .
