Reszel
Reszel ( , ) – miasto w woj .
warmińsko-mazurskim , w powiecie kętrzyńskim , siedziba gminy miejsko-wiejskiej Reszel .
Położone jest na Pojezierzu Mrągowskim , nad rzeką Sajną , dopływem Gubra wpadającego do Łyny .
Pod względem historycznym Reszel leży na Warmii , na obszarze dawnego pruskiego terytorium Barcji .
Miasto z wieloma zabytkami , należące do sieci cittàslow .
Według danych z 1 stycznia 2018 Reszel liczył 4615 mieszkańców .
Historia .
Epoka krzyżacka .
W 1241 wojska krzyżackie zdobyły gród pruskiego plemienia Bartów , który w tym czasie nosił staropruską nazwę Resel ( Reszel ) .
W miejscu zniszczonego grodu postawiono strażnicę .
Prusowie rok później , podczas powstania , odbili gród i utrzymali go przez pięć lat .
