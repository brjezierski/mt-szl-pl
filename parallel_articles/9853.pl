Retikulocyt
Retikulocyt , retykulocyt , proerytrocyt ( skrótowo Ret . )
– niedojrzała postać krwinki czerwonej , która w szlaku erytropoezy bezpośrednio poprzedza dojrzałą krwinkę czerwoną , czyli erytrocyt .
Jego powstawanie jest związane głównie z uzupełnianiem fizjologicznie niszczonych krwinek czerwonych oraz wyrównywaniem ( kompensacją ) skutków zdarzeń lub chorób w których dochodzi do utraty , czy niszczenia erytrocytów ( np .
krwotoków , zimnicy ) .
Proerytrocyty zostały odkryte w 1890 roku przez Paula Ehrlicha , niemieckiego bakteriologa i chemika .
Dojrzewanie retikulocytów .
Retikulocyty w procesie powstawania krwinek czerwonych powstają w szpiku kostnym z ortochromatycznych erytroblastów .
W ciągu 2–4 dni dojrzewają bezpośrednio do erytrocytu , dojrzałej krwinki czerwonej .
W stanie zdrowia do krwi przedostają się dwu-trzydniowe retikulocyty , gdzie w ciągu doby dochodzi do ich przekształcenia się do w pełni dojrzałego erytrocytu .
Liczba retikulocytów we krwi stanowi około 40 % ogólnej puli retikulocytów całego organizmu .
W stanach zmniejszonej liczności erytrocytów , czy to w wyniku choroby ( niedokrwistości ) , po urazach ( krwotoki ) , czy też fizjologicznie ( np .
u ludzi przebywających na dużych wysokościach n.p.m .
) , do krwi uwalniane są młodsze retikulocyty , czasem nawet jednodniowe .
Całkowity czas ich dojrzewania nie ulega jednak istotnym zmianom .
Budowa retikulocytów .
Retikulocyty są pozbawionymi jądra komórkami wielkości 8–10 μm , których charakterystyczną cechą jest obecność `` substantia reticulofilamentosa '' ( dosł .
`` substancji włókienkowej siateczki '' ) .
Siateczka ta jest pozostałością kwaśnych białek jądrowych zbudowanych z RNA .
Brak jądra i obecność kwasu rybonukleinowego jest więc cechą pozwalającą odróżnić je od innych komórek krwi lub szpiku kostnego .
Retikulocyty , zwłaszcza dojrzałe , są komórkami zdolnymi do przenoszenia tlenu .
Są jednak mniej wydajne od erytrocytów .
Zawierają & gt ; 80 % , a w przypadku retikulocytów krwi obwodowej & gt ; 95–97 % masy hemoglobiny dojrzałego erytrocytu .
Są większe od krwinek czerwonych , przez co stężenie hemoglobiny w retikulocytach jest mniejsze niż w erytrocytach .
Te dwa fakty – często niepełna pula hemoglobiny i jej niższe stężenie , tłumaczy mniejszą zdolność wiązania tlenu .
Retikulocyty wykazują wyższą oporność osmotyczną w porównaniu do erytrocytów .
Informacją istotną diagnostycznie może być określenie udziału młodych retikulocytów ( tzn .
tych , które typowo znajdują się w szpiku ) we krwi obwodowej .
W tym celu przyjęto podział retikulocytów umownie określający ich wiek .
Najstarszą klasyfikacją jest czteroklasowy podział według Ludwiga Heilmeyera – klasyfikacja Heilmeyera .
