Retimno
Retimno ( gr .
Ρέθυμνο ) – miasto w Grecji , na północnym wybrzeżu Krety , w administracji zdecentralizowanej Kreta , w regionie Kreta , w jednostce regionalnej Retimno .
Siedziba gminy Retimno .
W 2011 roku liczyło 32 468 mieszkańców .
Po Heraklionie i Chanii trzecie pod względem wielkości miasto na Krecie .
