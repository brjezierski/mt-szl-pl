Reunion
Reunion ( fr .
`` La Réunion '' ) – wyspa na Oceanie Indyjskim , położona około 700 km na wschód od Madagaskaru i 174 km na południowy zachód od Mauritiusu .
Stanowi ona najdalej wysuniętą na zachód część archipelagu Maskarenów .
Wyspa zajmuje powierzchnię 2512 km² .
Zamieszkuje ją 842 767 ( 2014 ) mieszkańców .
Reunion stanowi region administracyjny i jednocześnie departament Francji o statusie departamentu zamorskiego nr 974 ( od 1946 r. ) .
Wyspa dzieli się na 4 okręgi ( `` arrondissement '' ) i 49 kantonów .
Stolicą departamentu jest położone na północy wyspy Saint-Denis ( 142,6 tys .
mieszk . ) .
Inne ważne miasta to Saint-Paul ( 95,1 tys .
) , Saint-Pierre ( 74,7 tys .
) , Le Tampon ( 65,4 tys .
) , Saint-Louis ( 47,2 tys .
) , Saint-André ( 46,5 tys . )
i Le Port ( 41,5 tys .
) .
Geografia .
Wyspa jest pochodzenia wulkanicznego .
Najwyższym punktem jest szczyt wygasłego wulkanu Piton des Neiges – 3070,5 m n.p.m .
Za to niższy wulkan Piton de la Fournaise , o wysokości 2632 m n.p.m. , należy do najbardziej aktywnych wulkanów na świecie ( ostatnia erupcja 14 lipca 2017 ) .
Na wyspie panuje klimat zwrotnikowy .
Ochłodzenie przynoszą południowo-zachodnie pasaty , a wraz z nimi obfite opady deszczu oraz niekiedy gwałtowne cyklony .
Niegdyś porastały ją bujne lasy równikowe .
Większość z nich wycięto , a wraz z nimi wyginęło wiele gatunków zwierząt .
Przyroda .
Na terytorium zaobserwowano 2685 gatunków autochtonicznych zwierząt , w tym aż 697 występujących endemicznie .
