Reykjavík
Reykjavík ( dawny polski egzonim : `` Rejkiawik '' ) – stolica oraz największe miasto Islandii , położone w jej południowo-zachodniej części nad Zatoką Faxa , najbardziej wysunięta na północ stolica świata .
Na północ od Reykjavíku w odległości 10 km góruje masyw Esja .
Ludność islandzkiej stolicy wynosi 131,1 tys .
