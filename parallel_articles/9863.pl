Żyto zwyczajne
Żyto zwyczajne ( `` Secale cereale '' ) – gatunek zbóż z rodziny wiechlinowatych , najważniejszy pod względem gospodarczym przedstawiciel rodzaju żyto .
Pochodzi z Bliskiego Wschodu , ale uprawiany jest głównie w północno-wschodniej , wschodniej i środkowej Europie .
Gatunek uprawiany jest jako jednoroczna roślina ozima , rzadziej jara .
Uprawiana na glebach lekkich .
Cechuje się dużą odpornością na mróz i małymi wymaganiami glebowymi i cieplnymi .
Rozmieszczenie upraw związane jest z tradycją upraw , wykorzystaniem żyta jako paszy dla zwierząt gospodarskich oraz spożyciem żytniego chleba .
Po jego skrzyżowaniu z pszenicą uzyskano pszenżyto .
Uprawa .
Żyto uprawiane jest w chłodniejszych częściach Europy północnej i środkowej , a także w Rosji , Argentynie , Afryce Południowej , Kanadzie i Stanach Zjednoczonych .
Jest jednym z najbardziej odpornych zbóż , można je uprawiać na wszystkich rodzajach gleb , a rodzaj gleby nie wpływa znacząco na plony .
Żyto można uprawiać na glebach bardzo lekkich nawet w monokulturze .
Odporne jest również na susze i zimne temperatury .
Najkorzystniejsze temperatury : od siewu do kiełkowania to 10–13 °C , w czasie wegetacji wiosennej – średnia dobowa temp. , czyli ok. 7–8,3 °C , lecz roślina może również przetrwać pewien czas pokryta śniegiem .
Przyjmuje się , że optymalnym rozwiązaniem jest , kiedy od momentu siewu żyto ma do dyspozycji około 50 dni na odpowiednie przygotowanie się do nadejścia niskich temperatur .
Celem minimum jest natomiast rozpoczęcie procesu krzewienia przed przejściem w stan zimowego spoczynku .
Pola obsiane żytem nie muszą być silnie nawadniane ( najwięcej wody potrzebne jest w początkowej fazie wzrostu , tj .
od zasiania do wykiełkowania ) , gdyż sama roślina , ze względu na dobre ukorzenienie oraz mniejsze ulistnienie , potrzebuje mniej wody niż inne zboża .
