Rhachomyces lasiophorus
Rhachomyces lasiophorus ( Thaxt . )
Thaxt .
– rodzaj grzybów z rzędu owadorostowców ( `` Laboulbeniaceae '' ) .
Grzyby mikroskopijne , pasożyty stawonogów , głównie owadów ( grzyby entomopatogeniczne ) .
Charakterystyka .
Są obligatoryjnymi pasożytami zewnętrznymi owadów , to znaczy , że nie mogą przetrwać poza nimi i pasożytują na ich zewnętrznej powierzchni ciała .
Tworzą mikroskopijną , kilkukomórkową plechę , która tylko stopą wrasta do chitynowego oskórka owadów , cała plecha zaś wystaje ponad oskórkiem .
Nie powodują śmierci owadów i zazwyczaj wyrządzają im niewielką szkodę .
W Polsce Tomasz Majewski podał występowanie tego gatunku na chrząszczach ( Coleoptera ) z rodziny biegaczowatych ( Carabidae ) należących do gatunków : `` Acupalpus consputus '' , `` Acupalpus exiguus '' , `` Acupalpus flavicollis '' , `` Acupalpus parvulus '' , `` Badister dilatatus '' , `` Badister pellatus '' .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Rhachomyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał Roland Thaxter w 1892 r. nadając mu nazwę `` Acanthomyces lasiophorus '' .
Trzy lata później przeniósł go do rodzaju `` Rhachomyces '' .
