Corethromyces pallidus
Corethromyces pallidus ( Thaxt . )
Thaxt .
– gatunek grzybów z rzędu owadorostowców ( `` Laboulbeniales '' ) .
Grzyby mikroskopijne , pasożyty owadów ( grzyby entomopatogeniczne ) .
Systematyka .
Pozycja w klasyfikacji według Index Fungorum : `` Corethromyces , Laboulbeniaceae , Laboulbeniales , Laboulbeniomycetidae , Laboulbeniomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Gatunek ten opisał w 1893 r. Roland Thaxter r. jako `` Rhadinomyces pallidus '' .
W 1912 r. ten sam autor przeniósł go do rodzaju `` Corethromyces '' .
Charakterystyka .
Jest pasożytem zewnętrznym .
W Polsce Tomasz Majewski w 1994 i 2003 r. opisał jego występowanie na chrząszczach z rodziny kusakowatych ( Staphylinidae ) : `` Lathrobium quadratum '' , `` Lathrobium scutellare '' , `` Lathrobium terminatum '' .
