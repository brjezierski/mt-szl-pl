Rheinfelden ( Baden )
Rheinfelden ( Baden ) ( do 7 maja 1963 `` Rheinfelden '' ; alem .
`` Badisch-Rhyfälde '' ) – miasto w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Fryburg , w regionie Hochrhein-Bodensee , w powiecie Lörrach , siedziba wspólnoty administracyjnej Rheinfelden ( Baden ) .
Leży nad Renem , przy granicy ze Szwajcarią ( pod drugiej stronie Renu znajduje się miasto Rheinfelden ) , ok. 15 km na wschód od Bazylei .
Przez teren miasta przebiega autostrada A98 , droga krajowa B34 oraz B316 .
W mieście znajdują się przystanki i stacje kolejowe : Herten , Rheinfelden ( Baden ) , Beuggen , leżą one na linii kolejowej Hochrheinbahn .
