Wzorzec pstry
Wzorzec pstry ( `` Rhizocarpon petraeum '' ( Wulfen ) A. Massal .
– gatunek grzybów z rodziny `` Rhizocarpaceae '' .
Ze względu na współżycie z glonami zaliczany jest do grupy porostów .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Rhizocarpon , Rhizocarpaceae , incertae sedis , Lecanoromycetidae , Lecanoromycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1787 r. Franz Xaver von Wulfen nadając mu nazwę `` Lichen petraeus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1852 r. Abramo Bartolommeo Massalongo , przenosząc go do rodzaju `` Rhizocarpon '' .
