Rozłożek czerniejący ( `` Rhizopus stolonifer '' ( Ehrenb . )
Vuill . )
– gatunek grzybów należący do typu pleśniakowców ( `` Mucorales '' ) .
Pospolicie występuje w glebie , na owocach , warzywach i na produktach spożywczych powodując ich pleśnienie .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Rhizopus , Rhizopodaceae , Mucorales , Incertae sedis , Mucoromycetes , Mucoromycotina , Mucoromycota , Fungi '' .
Po raz pierwszy opisał go w 1753 r. Karol Linneusz , ale utożsamiał go z pleśniakiem białym ( `` Mucor mucedo '' ) .
Jako odrębny gatunek zdiagnozował go w 1818 r. Christian Gottfried Ehrenberg , nadając mu nazwę `` Mucor stolonifer '' .
Ten sam autor w 1821 r , przeniósł go do rodzaju `` Rhizopus '' , jako `` Rhizopus nigricans '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1902 r. Jean Paul Vuillemin .
