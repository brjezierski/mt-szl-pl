Rhodocybe truncata
Rhodocybe truncata ( Schaeff . )
Singer – gatunek grzybów z rodziny dzwonkówkowatych ( `` Entolomataceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Rhodocybe , Entolomataceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1774 r. Schaeffer nadając mu nazwę `` Agaricus truncatus '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1947 r. Rolf Singer , przenosząc go do rodzaju `` Rhodocybe '' .
