Agnieszka Dziemianowicz-Bąk
Agnieszka Ewa Dziemianowicz-Bąk ( ur .
20 stycznia 1984 we Wrocławiu ) – polska pedagog , filozofka , badaczka społeczna , doktor nauk humanistycznych , działaczka społeczna i polityczna , posłanka na Sejm IX kadencji .
Życiorys .
Wykształcenie , działalność naukowa i zawodowa .
Absolwentka Liceum Ogólnokształcącego nr III im .
Adama Mickiewicza we Wrocławiu ( 2003 ) .
Ukończyła pedagogikę na Wydziale Nauk Historycznych i Pedagogicznych ( 2008 ) oraz filozofię na Wydziale Nauk Społecznych ( 2009 ) na Uniwersytecie Wrocławskim .
W 2009 rozpoczęła studia doktorskie w Instytucie Filozofii UWr .
W 2018 uzyskała stopień naukowy doktora nauk humanistycznych w zakresie filozofii na podstawie napisanej pod kierunkiem profesora Adama Chmielewskiego pracy pt .
`` Reprodukcja – Opór – Upełnomocnienie .
Radykalna krytyka edukacji we współczesnej zachodniej myśli społecznej '' .
Autorka artykułów i publikacji naukowych .
W maju 2010 podjęła stałą współpracę z Ośrodkiem Myśli Społecznej im .
Ferdynanda Lassalle ’ a we Wrocławiu .
