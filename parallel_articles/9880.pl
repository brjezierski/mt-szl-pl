Rhynchosporium secalis
Rhynchosporium secalis ( Oudem . )
Davis – gatunek grzybów z klasy patyczniaków ( `` Helotiales '' ) .
U zbóż i niektórych traw wywołuje chorobę o nazwie rynchosporioza zbóż .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Ploettnerulaceae , Helotiales , Leotiomycetidae , Leotiomycetes , Pezizomycotina , Ascomycota , Fungi '' .
Po raz pierwszy takson ten zdiagnozował w 1897 r. Cornelius Anton Jan Abraham Oudemans nadając mu nazwę `` Marrsonia secalis '' .
W 1897 r. Ernst Heinsen przeniósł go do rodzaju `` Rhynchosporium '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu w 1919 r. John Jefferson Davis .
Synonimy nazwy naukowej :
Morfologia i rozmnażanie .
Grzyb mikroskopijny .
