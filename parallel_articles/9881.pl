Rhäzüns
Rhäzüns ( rm .
`` Razén '' ) – miejscowość i gmina w Szwajcarii , w kantonie Gryzonia , w regionie Imboden .
Pod względem powierzchni jest najmniejszą gminą w regionie .
Demografia .
W Rhäzüns mieszka 1 611 osób .
W 2020 roku 20,7 % populacji gminy stanowiły osoby urodzone poza Szwajcarią .
Transport .
Przez teren gminy przebiega droga główna nr 13 .
