Richard Blackwell
Richard Blackwell , właśc .
Richard Sylvan Selzer ( ur .
29 sierpnia 1922 r. w Nowym Jorku , zm .
19 października 2008 w Los Angeles ) – amerykański projektant i krytyk mody , osobowość radiowa i telewizyjna , dziennikarz , za młodu także aktor dziecięcy .
Blackwell był pochodzenia żydowskiego .
Zmienił nazwisko na Dick Ellis , by później przybrać nazwisko Blackwell .
Najbardziej znany był z publikowanej corocznie w styczniu , począwszy od 1960 roku , listy 10 najgorzej ubranych kobiet ( `` Mr. Blackwell ’ s Ten Worst Dressed Women '' ) .
Był otwarcie deklarującym się gejem ; przez szereg lat jego partnerem był Robert Spencer .
