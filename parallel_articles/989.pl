Agost
Agost − miasto we wschodniej Hiszpanii w regionie Walencja w prowincji Alicante , w odległości 18 km od Alicante .
Ze względu na odległość od Morza Śródziemnego ( wybrzeże Costa Brava ) miasto nie jest typowym kurortem , lecz miejscowością przemysłową .
Znajdują się tu m.in .
: fabryki produkujące elementy budowlane oraz fabryki wyrobów czy fabryki ceramiki .
Agost znajduje się na skraju przybrzeżnej równiny otaczającej miasto Alicante , gdzie teren się wznosi ( Sierra del Ventós , Maigmó ) .
