Spinka miodowa
Spinka miodowa ( `` Rickenella mellea '' ( Singer & amp ; Clémençon ) Lamoure ) – gatunek grzybów należący do rodziny `` Rickenellaceae '' .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji : `` Rickenella , Rickenellaceae , Hymenochaetales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy opisali go w 1971 r. Rolf Singer i Heinz Clémençon , nadając mu nazwę `` Gerronema melleum '' .
Obecną , uznaną przez Index Fungorum nazwę nadał mu Denise Lamoure w 1979 r .
Polską nazwę zaproponował w 2003 r. Władysław Wojewoda .
Morfologia .
Średnica do 1 cm , początkowo dzwonkowaty , potem coraz bardziej wklęsły , pępkowaty .
