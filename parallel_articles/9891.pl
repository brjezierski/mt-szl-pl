Ridder
Ridder ( kaz .
i ros .
: Риддер ) – miasto o znaczeniu obwodowym w północno-wschodnim Kazachstanie , w obwodzie wschodniokazachstańskim , w Ałtaju Kruszcowym .
W 2009 roku liczyło ok. 50,5 tys .
mieszkańców .
Ośrodek wydobycia rud ołowiu , cynku , złota i metali rzadkich , hutnictwa metali nieżelaznych oraz przemysłu drzewnego , spożywczego i lekkiego .
W mieście funkcjonują 10 szkół ogólnokształcących , dziecięca szkoła plastyczna , dziecięca szkoła muzyczna , koledż agrarno-techniczny , 2 szkoły techniczne , ponadto znajdują się muzeum krajoznawcze , dom kultury i Ałtajski Ogród Botaniczny .
Historia .
Miejscowość została założona w 1786 roku i nazwana na cześć Filippa Riddera , który odkrył w tym miejscu złoża rud polimetalicznych .
Prawa miejskie uzyskała w 1934 roku .
W latach 1941–2002 miasto funkcjonowało pod nazwą Leninogorsk ( Leninogor ) .
