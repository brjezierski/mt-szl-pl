Rieti ( stacja kolejowa )
Rieti ( włoski : `` Stazione di Rieti '' ) – stacja kolejowa w Rieti , w prowincji Rieti , w regionie Lacjum , we Włoszech .
Znajduje się na linii Terni – Sulmona .
Stacja jest zarządzana przez Rete Ferroviaria Italiana ( RFI ) .
Historia .
Stacja została otwarta w 1883 roku wraz z otwarciem odcinka Rocca di Corno – Terni .
