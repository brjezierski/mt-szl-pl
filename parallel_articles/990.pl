Agra
Agra ( hindi : आगरा ) – miasto w Indiach w stanie Uttar Pradesh nad Jamuną .
Populacja miasta w 2001 roku wynosiła 1 259 979 mieszkańców .
Siedziba rzymskokatolickiej archidiecezji Agra .
W mieście rozwinięty jest przemysł obuwniczy i włókienniczy oraz rzemiosło .
Ośrodek turystyczny .
Agra jest ważnym węzłem komunikacyjnym , znajduje się tam również uniwersytet .
Geografia i klimat .
Agra znajduje się w stanie Uttar Pradesh .
Miasto położone jest na południowym brzegu Jamuny , w miejscu przecięcia się historycznych szlaków handlowych , z których pierwszy prowadzi z Delhi wzdłuż Jamuny i Gangesu do Benares , a następnie do Bengalu , a drugi łączy Dekan na południu z przedpolem Himalajów i Kaszmirem .
Od południa ogranicza je kanał przekopany wzdłuż Jamuny .
Miasto leży w południowo-zachodniej części Niziny Hindustańskiej , na terenie płaskiej równiny aluwialnej , urozmaiconej niewielkimi pagórkami .
