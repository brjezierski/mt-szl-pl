Rijad
Rijad ( arab .
الرياض , `` Ar-Riyāḍ '' ) – stolica Arabii Saudyjskiej położona w środkowej części kraju , u wschodniego podnóża gór Dżabal Tuwajk , siedziba administracyjna prowincji Rijad .
W 2010 roku miasto liczyło ok. 5,2 mln mieszkańców .
Nazwa .
Nazwa `` Rijad '' pochodzi od arabskiego słowa الرياض ( `` '' ) , które oznacza `` miejsce ogrodów i drzew '' .
Dzięki pobliskiemu sąsiedztwu licznych uedów , okolice Rijadu od czasów antycznych były żyznym obszarem , ulokowanym w samym sercu pustynnego Półwyspu Arabskiego .
