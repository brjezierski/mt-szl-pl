Rimini ( stacja kolejowa )
Rimini ( wł : `` Stazione di Rimini '' ) – stacja kolejowa w Rimini , w prowincji Rimini , w regionie Emilia-Romania , we Włoszech .
Jest jedną z największych stacji kolejowych w regionie .
Została otwarta w 1861 .
Jest zarządzana przez Rete Ferroviaria Italiana oraz Centostazioni .
Oprócz Rimini obsługuje również San Marino .
Obsługuje około 5 mln pasażerów rocznie .
