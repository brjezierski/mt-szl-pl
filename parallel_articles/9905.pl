Ringelai
Ringelai – miejscowość i gmina w Niemczech , w kraju związkowym Bawaria , w rejencji Dolna Bawaria , w regionie Donau-Wald , w powiecie Freyung-Grafenau .
Leży w Lesie Bawarskim , około 5 km na zachód od miasta Freyung .
Dzielnice .
W skład gminy wchodzą trzy dzielnice : Ringelai , Wasching i Eckertsreut .
Oświata .
W gminie znajduje się 65 miejsc przedszkolnych ( 53 dzieci ) oraz szkoła podstawowa ( 8 nauczycieli , 108 uczniów ) .
