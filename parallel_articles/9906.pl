Hemachatus haemachatus
Hemachatus haemachatus – gatunek jadowitego węża z rodziny zdradnicowatych ( Elapidae ) , występujący w południowej części Afryki .
Wspólnie z kilkoma innymi gatunkami określany jest zbiorczo mianem kobry plującej , gdyż zagrożony potrafi celnie pluć jadem z odległości nawet ponad 2 metrów , celując w oczy napastnika , wywołując w ten sposób silny ból .
Jad ma właściwości neurotoksyczne i cytotoksyczne .
Opis .
Ubarwienie jest zróżnicowane w zależności od obszaru występowania .
Charakterystyczną cechą jest ciemny spód i jasny pierścień ( niekiedy dwa ) na szyi , stąd potoczna nazwa w RPA , `` Ringhals '' .
Zachowanie .
Zagrożony , podobnie jak inne kobry , rozpościera kaptur , ukazując również charakterystyczny pierścień na szyi .
Pożywienie .
Żywi się głównie ropuchami , czasami niewielkimi ssakami i gadami .
Poluje nocą .
Środowisko .
Preferuje tereny trawiaste .
