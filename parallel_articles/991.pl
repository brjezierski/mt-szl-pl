Agria
Agria ( gr .
`` Αγριά '' ) – nadmorska miejscowość w Grecji , w administracji zdecentralizowanej Tesalia-Grecja Środkowa , w regionie Tesalia , w jednostce regionalnej Magnezja , w gminie Wolos , w odległości ok. 7 km od miasta Wolos .
W 2011 roku liczyła 5191 mieszkańców .
Miejsce narodzin Vangelisa , twórcy muzyki elektronicznej .
