Rita Ora
Rita Ora-Waititi ( ur .
26 listopada 1990 w Prisztinie ) – angielska wokalistka pochodzenia albańskiego , aktorka i autorka tekstów .
Życiorys .
Urodziła się w Prisztinie , mieście leżącym wówczas na terenie Socjalistycznej Federacyjnej Republice Jugosławii ( dzisiejsze Kosowo ) , w albańskiej rodzinie .
Jako roczne dziecko wyemigrowała wraz z matką jako uchodźcy polityczni do Wielkiej Brytanii .
Dorastała w zachodnim Londynie , uczęszczając do szkoły St Cuthbert & amp ; St Matthias w Earl ` s Court , a później do Sylvia Young Theatre School .
W 2004 zagrała w filmie `` Kłopotliwy towar '' .
W 2008 pojawiła się gościnnie w piosence `` Where 's Your Love '' Craiga Davida .
Rok później wzięła udział w castingu , który miał wyłonić brytyjskiego reprezentanta do 54 .
Konkursu Piosenki Eurowizji w 2009 , ale w późniejszym etapie wycofała się z tego pomysłu .
W tym czasie coraz częściej pojawiała się w londyńskich barach , prezentując swój potencjał wokalny , szybko wzbudzając zainteresowanie poszukiwaczy talentów z Roc Nation .
Wkrótce rozpoczęła współpracę z raperem Jay-Z oraz Drake .
27 sierpnia 2012 wydała swój debiutancki album studyjny pt .
`` Ora '' , który zadebiutował na szczycie bestsellerów w Wielkiej Brytanii i Szkocji , sprzedając się w liczbie 40 tys .
