Agrinio
Agrinio ( gr .
`` Αγρίνιο '' , łac .
`` Agrinium '' ) – miasto w zachodniej Grecji , w administracji zdecentralizowanej Peloponez , Grecja Zachodnia i Wyspy Jońskie , w regionie Grecja Zachodnia , w jednostce regionalnej Etolia-Akarnania .
Siedziba gminy Agrinio .
W 2011 roku liczyło 46 899 mieszkańców .
