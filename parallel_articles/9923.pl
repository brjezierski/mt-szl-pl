Robert Biedroń
Robert Biedroń ( ur .
13 kwietnia 1976 w Rymanowie ) – polski polityk , samorządowiec , ekspert organizacji praw człowieka , działacz na rzecz praw osób LGBTQ+ i publicysta .
Inicjator i w latach 2001–2009 prezes Kampanii Przeciw Homofobii .
Poseł na Sejm VII kadencji ( 2011–2014 ) , pierwszy otwarcie deklarujący się gej w polskim parlamencie .
W latach 2014–2018 prezydent Słupska , od 2019 deputowany do Parlamentu Europejskiego IX kadencji .
Od 2016 członek grupy doradczej do spraw równouprawnienia płci , przymusowych wysiedleń i ochrony przy Organizacji Narodów Zjednoczonych .
Założyciel i w latach 2019–2021 prezes partii Wiosna , obok Włodzimierza Czarzastego i Adriana Zandberga jeden z trzech liderów porozumienia Lewica ( 2019 ) i szef sztabu Lewicy w kampanii przed wyborami parlamentarnymi w 2019 , od 2021 współprzewodniczący Nowej Lewicy ( która powstała z połączenia Wiosny i SLD ) .
Kandydat na urząd prezydenta RP w pierwszych i drugich wyborach w 2020 .
Życiorys .
Młodość i wykształcenie .
Urodził się 13 kwietnia 1976 w Rymanowie , a wychował w Krośnie .
Jest synem Zdzisława ( 1954–2008 ) i Heleny .
Ma dwóch młodszych braci i młodszą siostrę .
Jego matka należała do Niezależnego Samorządnego Związku Zawodowego „ Solidarność ” , a ojciec do Polskiej Zjednoczonej Partii Robotniczej .
Wychował się w rodzinie dotkniętej przemocą domową i chorobą alkoholową .
Jego rodzice zapisali go do technikum hotelarskiego w Ustrzykach Dolnych , ponieważ uczono tam trzech języków obcych ( francuskiego , rosyjskiego i angielskiego ) , a Robert Biedroń wykazywał zainteresowanie językami obcymi .
W 1991 wyjechał do Ustrzyk Dolnych i zamieszkał w internacie .
