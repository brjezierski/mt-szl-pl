Robert Mulligan
Robert Patrick Mulligan ( ur .
23 sierpnia 1925 w Bronx w Nowym Jorku , zm .
20 grudnia 2008 w Lyme ) – amerykański reżyser filmowy , nominowany w 1963 do Oscara za reżyserię filmu `` Zabić drozda '' .
Życiorys .
Urodził się na Brooklynie w Nowym Jorku jako syn policjanta Jamesa Mulligana .
Miał dwóch braci : Richarda ( 1932-2000 ) i Jamesa .
Podczas II wojny światowej służył w United States Marine Corps .
W młodości przez krótki czas pracował w redakcji magazynu `` New York Times '' , potem studiował dziennikarstwo i literaturę na Fordham University .
Po ukończeniu studiów rozpoczął pracę w stacji telewizyjnej CBS .
Początkowo jako posłaniec , później awansował na stanowisko asystenta produkcji .
W 1949 zaproponowano mu wyreżyserowanie serialu `` Suspense '' ( w wolnym tłumaczeniu na polski `` Napięcie '' ) , który realizował w latach 1949-1954 .
Największe uznanie przyniósł mu film `` Zabić drozda '' ( 1962 ) .
Film zdobył 3 Oscary , a Mulliganowi przyniósł jedyną nominację do tej nagrody w jego reżyserskiej karierze .
Potem nakręcił jeszcze 15 filmów , ale żaden nie odniósł już takiego sukcesu .
Na początku lat 90. wycofał się z zawodu .
W ostatnich latach życia ciężko chorował na serce .
Zmarł w wieku 83 lat w swoim domu w mieście Lyme w stanie Connecticut , w wyniku rozległego zawału serca .
