Robin Hood ( film 1922 )
Robin Hood ( ang .
`` Douglas Fairbanks in Robin Hood '' ) – amerykański niemy film płaszcza i szpady z 1922 roku , w reżyserii Allana Dwana na podst .
legend ludowych o Robin Hoodzie , z udziałem Douglasa Fairbanksa i Wallace ' a Beery ’ ego .
To pierwszy film w historii , którego premiera odbyła się w Hollywood , oraz jeden z najdroższych obrazów lat 20. z budżetem szacowanym na półtora miliona dolarów .
Fabuła .
W średniowiecznej Anglii earl Huntingdon jest ulubieńcem króla Ryszarda Lwie Serce biorąc udział w turniejach rycerskim i wykazując nieśmiałość w kontaktach z kobietami .
Podczas jednej z uczt Huntingdon nawiązuje romans z dwórką lady Marian Fitzwalter , jak broni ją przed napastującym księciem Janem , bratem Ryszarda .
Wkrótce wraz z królem wyrusza na wyprawę krzyżową .
Na czas swojej nieobecności król przekazuje władzę Janowi , który wkrótce objawia się jako okrutny i bezwzględny tyran uciskający ludność podatkami za pomocą nowo mianowanych szeryfów .
Kiedy będący we Francji Huntingdon otrzymuje wiadomość od lady Marian i dowiaduje się o wszystkim , prosi króla o zgodę na powrót do Anglii .
Ten jednak odmawia uznając , że earl stchórzył .
Mimo tego Huntingdon i jego giermek nadal szukają sposobu na powrót , ale zostają wciągnięci w zasadzkę sługę Jana , sir Guya Gisbourne ’ a i uwięzieni pod zarzutem dezercji .
Udaje im się jednak zbiec i wrócić do Anglii .
Tymczasem Jan po torturach służki lady Marian dowiaduje się o jej spiskowaniu i ta musi się ukrywać .
Finguje swą śmierć w upadku w przepaść nieopodal Lasu Sherwood , gdzie azyl mają inne ofiary rządów Jana .
Huntingdon dowiaduje się o rzekomej śmierci Marian i postanawia wystąpić przeciwko Janowi .
Rok później przyjmuje imię Robin Hood i zaczyna rabować bogatych , żeby dawać biednym .
