Robin Williams
Robin McLaurin Williams ( ur .
21 lipca 1951 w Chicago , zm .
11 sierpnia 2014 w Paradise Cay ) – amerykański komik i aktor .
Laureat Oscara w kategorii „ Najlepsza rola drugoplanowa ” za rolę psychologa w dramacie `` Buntownik z wyboru '' ( 1997 ) oraz trzykrotnie nominowany do nagrody za występ w filmach : `` Good Morning , Vietnam '' ( 1987 ) , `` Stowarzyszenie Umarłych Poetów '' ( 1989 ) , i `` Fisher King '' ( 1991 ) .
Życiorys .
Wczesne lata .
Urodził się w Chicago w Illinois w dobrze sytuowanej rodzinie Laurie McLaurin ( z domu Smith ; ur .
1922 , zm .
2001 ) i Roberta Fitzgeralda Williamsa ( ur .
10 września 1906 , zm .
18 października 1987 ) .
Jego ojciec był menedżerem w Ford Motor Company , a matka – profesjonalną modelką .
Jako dziecko był samotny , żył we własnym wyobrażonym świecie , jak w świecie przyjaciół i gier dziecięcych .
Uczęszczał do Detroit Country Day School w Beverly Hills .
W 1969 ukończył Redwood High School w Larkspur .
Następnie studiował nauki polityczne w męskiej uczelni Claremont Men 's College w Claremont .
Uczęszczał na lekcje dramatu w College of Marin w Hrabstwie Marin .
