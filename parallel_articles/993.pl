Polówka błotna
Polówka błotna ( `` Agrocybe paludosa '' ( J.E .
Lange ) Kühner & amp ; Romagn .
ex Bon ) – gatunek grzybów z rodziny pierścieniakowatych ( `` Strophariaceae '' ) .
Systematyka i nazewnictwo .
Pozycja w klasyfikacji według Index Fungorum : `` Agrocybe , Strophariaceae , Agaricales , Agaricomycetidae , Agaricomycetes , Agaricomycotina , Basidiomycota , Fungi '' .
Po raz pierwszy gatunek ten opisał w 1921 r. Jakob Emanuel Lange pod nazwą `` Pholiota praecox '' var .
`` paludosa '' .
Obecną , uznaną przez Index Fungorum nazwę nadali mu Robert Kühner , Henri Charles Louis Romagnesi i Marcel Bon w 1987 r .
Synonimy :
Polską nazwę zaproponował Władysław Wojewoda w 1999 r .
Morfologia .
Średnica do 4 cm , u młodych owocników wypukły , potem rozprostowujący się , w końcu prawie płaski .
Świeży jest lepki , ale szybko wysycha .
Powierzchnia naga , o barwie od kremowej do pomarańczowej , wyraźnie blednie w miarę wysychania .
