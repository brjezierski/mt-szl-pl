Robin van Persie
Robin van Persie ( wym .
; ur .
6 sierpnia 1983 w Rotterdamie ) – holenderski piłkarz grający na pozycji napastnika , reprezentant kraju .
Rodzice van Persiego są artystami , lecz on – zamiast pójść w ich ślady – rozpoczął grę w młodzieżówce Excelsioru Rotterdam .
Później trafił do innego klubu z Rotterdamu – Feyenoordu .
Spędził tam trzy sezony i w 2002 roku zdobył Puchar UEFA .
Z powodu licznych nieporozumień z trenerem Bertem van Marwijkiem van Persie postanowił zmienić klub i w 2004 roku za kwotę 2,75 miliona funtów został sprzedany do Arsenalu .
W swoim pierwszym sezonie w Londynie wygrał Tarczę Wspólnoty i Puchar Anglii .
W 2006 roku zdobył nagrodę Rotterdam Sportsman of the year .
W czasie pierwszych czterech sezonów van Persiego w Arsenalu trapiły go kontuzje , co zredukowało liczbę jego występów w pierwszym zespole .
Latem 2012 roku trafił do Manchesteru United .
W lipcu 2015 roku przeszedł do tureckiego Fenerbahçe SK .
W styczniu 2018 powrócił do Feyenoordu , a w 2019 zakończył tam karierę .
W reprezentacji Holandii van Persie zadebiutował w roku 2004 .
