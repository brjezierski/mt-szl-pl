Roblox
Roblox – edukacyjna gra internetowa ( określana też jako prosta platforma do tworzenia gier ) wydana w 2006 roku przez amerykańskie studio Roblox Corporation .
Uczestnicy gry samodzielnie kształtują rozgrywkę poprzez tworzenie przedmiotów , miejsc akcji itp .
Możliwe jest udostępnianie własnych map zbudowanych z modułów przypominających klocki Lego .
Rozgrywka opiera się na modelu free-to-play , ale gracze mogą też płacić za zakup wirtualnej waluty zwanej „ robuxem ” .
Według danych z 2017 roku w `` Robloksa '' każdego miesiąca gra średnio 50 milionów graczy .
Do tworzenia i edytowania miejsc służy program o nazwie Roblox Studio , który pozwala na tworzenie różnych map i symulacji .
Przedmioty używane w grze podlegają prawom fizyki , zbliżonym do praw naturalnych .
Do odtwarzania gier przygotowanych na platformie służy Roblox Client .
`` Roblox '' jest dostępny na platformach Windows ( również jako aplikacja w sklepie Microsoft ) , macOS , Xbox One , Android , iOS oraz Fire OS .
Wykorzystuje gogle wirtualnej rzeczywistości Oculus Rift oraz HTC Vive .
Produkcja kierowana jest do graczy w wieku 8–18 lat .
Rozgrywka .
Akcja gry toczy się w świecie wykreowanym przez samych graczy – tworzą np .
własne postacie , poziomy oraz zadania .
Producenci udostępnili do dyspozycji program Roblox Studio , który umożliwia tworzenie własnych scenariuszy gry oraz wykorzystania skryptów Lua .
W `` Robloksie '' dostępna jest wirtualna waluta zwana „ robuxem ” ( w marcu 2016 ogłoszono rezygnację z waluty zwanej „ ticketami ” ) , umożliwiająca kupno dodatkowych przedmiotów .
Kupuje się ją za prawdziwe pieniądze .
Użytkownicy mogli też zapisać się do „ Builders Club ” poprzez wykupienie abonamentu .
23 września 2019 nazwę płatnego pakietu zmieniono na „ Roblox Premium ” .
