Józefa .
Jego matka chciała , by został księdzem .
W szkole był nieśmiały , ale nie zamknięty w sobie .
Przez pięć lat uczęszczał do technikum elektronicznego .
Mając 17 lat wyjechał do Paryża , do swojego starszego brata Armando Edoardo .
Tam zgłosił się do marynarki handlowej .
Był kelnerem w pizzerii Pizza Pino na Polach Elizejskich , a następnie pomagał swojemu bratu w pracy w sieci restauracji Casa Nostra , naprzeciwko Wieży Eiffla .
Dorabiał również w dyskotekach i domach publicznych .
