15 marca 1991 ) , gospodyni domowej , i Gennaro Tano ( ur .
1924 , zm .
15 lipca 2012 ) , pracownika robót publicznych w mieście .
Jego dziadek był hodowcą byków rozpłodowych .
Miał czterech braci – Claudio , Giorgio , Armando i Antonio – oraz siostrę Mirellę .
W 1971 jego starszy brat Claudio zmarł w wieku 12 lat , przyczyną śmierci był atak padaczki .
