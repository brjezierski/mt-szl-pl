Rocco Siffredi
Rocco Siffredi , właściwie Rocco Antonio Tano ( ur .
4 maja 1964 w Ortonie ) – włoski aktor , reżyser , scenarzysta i producent filmów pornograficznych .
Znany również jako „ włoski ogier ” .
Nazywany „ ojcem chrzestnym ostrego seksu ” .
Karierę zawodową rozpoczął w 1987 roku , gdy miał 23 lata .
Stał się tak samo popularny we Włoszech jak Ron Jeremy w Stanach Zjednoczonych .
W 2002 został umieszczony na dziesiątym miejscu na liście 50. największych gwiazd branży porno wszech czasów przez periodyk „ Adult Video News ” .
Swój pseudonim artystyczny przyjął od nazwiska kinowego bohatera – Rocha Siffrediego , którego grał Alain Delon w dramacie kryminalnym `` Borsalino '' ( 1970 ) .
Życiorys .
Wczesne lata .
