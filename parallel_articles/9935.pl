Rocha ( grusza )
Grusza 'Pêra Rocha do Oeste ' – odmiana uprawna ( kultywar ) gruszy .
Odmiana produkowana jest wyłącznie w Portugalii , będąc najważniejszym owocem eksportowym tego kraju .
Sady zlokalizowane są na ograniczonym obszarze około 100 km² , co wynika z posiadanego przez owoc statusu Chronionej Nazwy Pochodzenia , nadanego przez Unię Europejską w 2003 .
Sady tej odmiany można zakładać tylko w rejonie Oeste , co znajduje odzwierciedlenie w pełnej nazwie kultywaru .
Siewka gruszy Rocha została odnaleziona przypadkowo w 1836 przez rolnika i handlarza końmi Pedro António Rocha w okolicach Sintry .
Komercyjne sady Rocha zaczęły się intensywnie rozwijać w latach 70. i 80 .
XX wieku .
Produkcja znacząco zaś wzrosła w latach 90 .
