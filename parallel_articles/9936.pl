Rochdale
Rochdale – miasto w północno-zachodniej Anglii ( Wielka Brytania ) , w hrabstwie Wielki Manchester , w dystrykcie metropolitalnym Rochdale , położone nad rzeką Roch .
Miasto jest oddalone o ok. 16 km ( 10 mil ) od Manchesteru i ok. 8 km ( 5 mil ) od Oldham .
W 2001 roku miasto liczyło 95 796 mieszkańców .
W mieście rozwinął się przemysł włókienniczy , chemiczny , maszynowy oraz elektrotechniczny .
