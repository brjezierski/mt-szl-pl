Agrogaster coneae
Agrogaster coneae '' D.A .
Reid – gatunek wielkoowocnikowych podstawczaków należący do rodziny gnojankowatych ( `` Bolbitiaceae '' ) , jedyny z rodzaju Agrogaster '' D.A .
Reid .
Taksonomia .
Gatunek ten został po raz pierwszy opisany przez Dereka Reida w artykule `` New or interesting records of Australian Basidiomycetes : IV '' , opublikowanym w „ Transactions of the British Mycological Society ” z 1986 r. W tej samej publikacji wyodrębniono dla tego gatunku nowy rodzaj , `` Agrogaster '' , który z uwagi na budowę skórki kapelusza i zabarwienie zarodników zaliczono do rodziny gnojankowatych ( `` Bolbitiaceae '' ) .
Nazwa rodzajowa złożona jest z pierwszego członu rodzaju `` Agrocybe '' w kombinacji z członem `` gaster '' , wskazującym na podobieństwo do tzw .
`` wnętrzniaków '' ( `` Gasteromycetes '' ) .
Charakterystyka .
Grzyby z gatunku `` Agrogaster coneae '' tworzą naziemne , agarykoidalne lub sekotioidalne owocniki o wysokości do 4 cm .
Kapesz średnicy 1,0-1,5 cm , o różnym pokroju , brązowawy w odcieniu sjeny nieco higrofaniczny ( suchy jaśniejszy ) , o gładkiej , woskowej powierzchni .
Blaszki w akgarykoidalnych owocnikach regularne , średnio gęste , wąsko przyrośnięte do trzonu .
W owocnikach sekotioidalnych hymenofor składa się z blaszek mocno anastomozujących , tworzących wewnątrz owocnika zamknięte komory .
Trzon o wysokości 1,5-2 ( do 4 ) cm i średnicy 0,2-0,25 cm .
Miąższ o barwach od kremowobiałej do sjeny .
Podstawki czterozarodnikowe , cienkościenne , hialinowe .
Zarodniki elipsoidalne lub łezkowate , o wymiarach ok. 10×5,5 μm , nieamyloidalne .
Występowanie .
Gatunek ten odkryto na Nowej Zelandii , pod zastrzalinami `` Podocarpus dacrydioides '' i `` Podocarpus spicatus '' .
