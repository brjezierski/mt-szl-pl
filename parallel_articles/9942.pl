Rockhampton
Rockhampton – miasto w Australii , w stanie Queensland , port nad rzeką Fitzroy , około 60 km od jej ujścia do zatoki Keppel ( Morze Koralowe ) .
Ludność – ok. 75 tys .
Przez miasto przechodzi zwrotnik Koziorożca , co uwiecznione jest stosownym monumentem .
Miastem partnerskim Rockhampton jest japońska miejscowość Ibusuki .
W mieście znajdują się ubojnie i zakłady przetwórstwa mięsnego , co dało asumpt do nazwania go „ Miastem Byków ” .
W mieście rozwinął się przemysł spożywczy , chemiczny , materiałów budowlanych oraz drzewny .
Urodził się tutaj jeden z najbardziej utytułowanych tenisistów w historii Rod Laver .
