Rocquencourt ( Yvelines )
Rocquencourt – miejscowość i dawna gmina we Francji , w regionie Île-de-France , w departamencie Yvelines .
W 2016 roku populacja ówczesnej gminy wynosiła 3371 mieszkańców .
W dniu 1 stycznia 2019 roku z połączenia dwóch ówczesnych gmin – Le Chesnay oraz Rocquencourt – powstała nowa gmina Le Chesnay-Rocquencourt .
Siedzibą gminy została miejscowość Le Chesnay .
