Rod Steiger
Rod Steiger , właśc .
Rodney Stephen Steiger ( ur .
14 kwietnia 1925 w Westhampton , zm .
9 lipca 2002 w Los Angeles ) – amerykański aktor filmowy , teatralny i telewizyjny pochodzenia francuskiego , szkockiego i niemieckiego .
Laureat Oscara .
Był aktorem bardzo wszechstronnym , jego filmografia liczy ponad 100 pozycji .
Życiorys .
Wczesne lata .
Urodził się w Westhampton na Long Island w rodzinie luterańskiej jako syn Lorraine ( z domu Driver ) i Fredericka Steigera .
Uczęszczał do szkoły publicznej w Irvington , Bloomfield , Newark w stanie New Jersey .
W wieku 16 lat zaciągnął się do Marynarki Wojennej i w czasie II wojny światowej służył na Pacyfiku ( 1941–45 ) .
Kariera .
Po wojnie rozpoczął karierę teatralną , grając w spektaklach : `` Curse you , Jack Dalton ! ''
( 1946 ) w Civic Repertory Theatre of Newark i `` Proces Mary Dugan '' ( `` The Trial of Mary Dugan '' ) .
