Rodatycze
Rodatycze ( ukr .
Родатичі , `` Rodatyczi '' , dawniej Городятичі , `` Horodiatyczi '' ) – wieś położona w rejonie gródeckim w obwodzie lwowskim na Ukrainie , na Płaskowyżu Tarnogrodzkim .
W II Rzeczypospolitej miejscowość była siedzibą gminy wiejskiej Rodatycze .
Pierwsza wzmianka o wsi pochodzi z 1445 r. Wieś należała do posiadłości starostwa gródeckiego .
Właścicielami był rząd austriacki , Jan Machan i Antonina Mirska , która w 1890 r. przekazała posiadłość założonemu przez siebie Zgromadzeniu Sióstr Opatrzności we Lwowie .
Taki stan własności utrzymał się aż do września 1939 r. Wieś od 2 połowy XIX wieku miała połączenie kolejowe .
W 1904 roku wieś liczyła 2177 mieszkańców , zaś w 1929 roku wieś zamieszkiwało 2561 mieszkańców .
