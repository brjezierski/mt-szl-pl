Aguascalientes ( miasto )
Aguascalientes – miasto w środkowym Meksyku , na przedgórzu Sierra Madre Zachodnia , stolica stanu Aguascalientes oraz gminy o tej samej nazwie .
Leży nad rzeką Río Aguascalientes .
W 2014 roku miasto liczyło około 766,4 tysięcy mieszkańców .
Wraz z miejscowościami Jesus María i San Francisco de los Romo tworzy obszar metropolitalny zamieszkały przez ponad milion mieszkańców .
Miasto jest głównym ośrodkiem gospodarczym stanu Aguascalientes .
