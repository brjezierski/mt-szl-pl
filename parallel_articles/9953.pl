Roeselare ( 1953 )
Roeselare ( M914 ) – belgijski trałowiec z lat 50 .
XX wieku , jedna z 26 zbudowanych dla Belgijskiej Marynarki Wojennej jednostek amerykańskiego typu `` Adjutant '' .
Stępkę okrętu położono w lipcu 1951 roku w stoczni Hodgdon Brothers w East Boothbay , a w skład marynarki wojennej Belgii został przyjęty 22 września 1953 roku .
W marcu 1966 roku jednostka została przekazana Norwegii i wcielona do marynarki wojennej pod nazwą „ Tana ” .
W 1977 roku okręt przebudowano na niszczyciel min .
Jednostkę wycofano ze służby w 1997 roku .
Projekt i budowa .
Projekt trałowców typu `` Adjutant '' ( `` Blackbird '' ) powstał jako rozwinięcie drugowojennych jednostek typu `` YMS '' .
