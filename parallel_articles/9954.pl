Rogacze ( sielsowiet Indura )
Rogacze ( biał .
`` Рагачы '' , ros .
`` Рогачи '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Indura .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim , w gminie Indura .
Według Powszechnego Spisu Ludności z 1921 roku wieś zamieszkiwało 171 osób , 18 było wyznania rzymskokatolickiego a 153 prawosławnego .
Wszyscy mieszkańcy zadeklarowali polską przynależność narodową .
Było tu 35 budynków mieszkalnych .
Miejscowość należała do parafii rzymskokatolickiej i parafii prawosławnej w Indurze .
Podlegała pod Sąd Grodzki w Indurze i Okręgowy w Grodnie ; właściwy urząd pocztowy mieścił się w Indurze .
