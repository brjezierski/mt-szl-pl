Rogawka
Rogawka ( białorus .
Рогаўка ) – wieś w Polsce położona w województwie podlaskim , w powiecie siemiatyckim , w gminie Siemiatycze .
Prawosławni mieszkańcy wsi należą do parafii Cudownego Zbawiciela w Rogawce , a wierni kościoła rzymskokatolickiego parafii Wniebowzięcia Najświętszej Maryi Panny w Siemiatyczach .
Historia .
Wieś wzmiankowana w dokumentach pisanych w 1469 jako wieś królewska i w 1537 , gdy wymienione jest nazwisko jej wójta Wojciecha Nardułtowskiego ( Ardułtowskiego ? ) .
