Rogoziniec ( potok )
Rogoziniec – potok , prawostronny dopływ Bystrzycy Dusznickiej o długości 4,27 km i powierzchni zlewni 4,34 km² .
Potok płynie w Górach Bystrzyckich .
Jego źródła znajdują się pod szczytem Wolarza na wysokości ok. 800 m n.p.m. , uchodzi do Bystrzycy Dusznickiej w jej przełomowym odcinku zwanym Piekielną Doliną , na terenie Polanicy-Zdroju .
W swym dolnym odcinku obszar zlewni potoku wchodzi w skład ustanowionego tam obszaru stanowiącego część polskich obszarów Natura 2000 .
