Język agulski
Język agulski ( agulski агъул чIал , ros .
агульский язык ) – język Agulów , zamieszkujących południowo-wschodnią część Dagestanu .
Należy do zespołu samurskiego , podgrupy dagestańskiej w grupie północno-wschodniej ( nachsko-dagestańskiej ) języków kaukaskich .
Według ocen językiem tym posługuje się ok. 15–17 tys .
