Język agulski genetycznie najbliżej spokrewniony jest z językiem tabasarańskim .
Jego unikatową cechą jest odróżnianie spółgłosek nagłośniowych i krtaniowych .
Wyróżnia się dwa główne dialekty języka agulskiego : kuszański i agulski właściwy oraz trzy drobniejsze gwary lokalne .
Od początku lat 90. prowadzi się próby stworzenia piśmiennictwa w oparciu o cyrylicę rosyjską .
Powstały już pierwsze podręczniki do nauki języka dla dzieci , wydano także pierwsze zbiorki poezji w języku agulskim .
Studia nad językiem agulskim zapoczątkował niemiecki kaukazolog , Adolf Dirr ( 1867–1930 ) , publikując w 1907 r. w Tbilisi pracę pt .
