Rock
Rock – gatunek muzyki rozrywkowej powstały w połowie XX wieku w Stanach Zjednoczonych ( który wytworzył wokół siebie krąg subkultury młodzieżowej ) i ogólna nazwa szeregu stylów muzycznych , wywodzących się z rock and rolla oraz rhythm and bluesa i bluesa .
Sama nazwa „ rock ” jest skrótem od „ rock and roll ” , choć można uznawać owe dwa pojęcia za odmienne od siebie gatunki muzyczne .
