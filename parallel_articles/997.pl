Ahorn ( Badenia-Wirtembergia )
Ahorn – gmina w Niemczech , w kraju związkowym Badenia-Wirtembergia , w rejencji Stuttgart , w regionie Heilbronn-Franken , w powiecie Main-Tauber-Kreis , wchodzi w skład wspólnoty administracyjnej Boxberg .
Leży nad rzeką Erf , ok. 10 km na południowy zachód od Tauberbischofsheim , przy autostradzie A81 i linii kolejowej Stuttgart – Würzburg .
