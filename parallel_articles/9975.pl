Rokitno ( obwód grodzieński )
Rokitno ( biał .
`` Ракітна '' , ros .
`` Ракитно '' ) – wieś na Białorusi , w obwodzie grodzieńskim , w rejonie grodzieńskim , w sielsowiecie Wiercieliszki .
W dwudziestoleciu międzywojennym wieś leżała w Polsce , w województwie białostockim , w powiecie grodzieńskim .
