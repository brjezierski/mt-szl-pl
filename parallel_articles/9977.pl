Rolle ( miasto )
Rolle – szwajcarskie miasto na prawach gminy w kantonie Vaud , zamieszkiwane przez 6,218 osób ( wg stanu na rok 2017 ) .
Od 2006 przynależy do dystryktu Nyon .
Miejscowość położona jest na północno-zachodnim brzegu Jeziora Genewskiego pomiędzy Nyon a Lozanną .
Rolle usytuowane jest ok. 30 km na północny wschód od Genewy w znanym z winnic regionie La Côte .
Z miasteczka rozpościera się widok na alpejskie szczyty .
W Rolle mieszkał i zmarł Jean-Luc Godard ( 1930-2022 ) , wybitny francuski reżyser filmowy i współtwórca Nowej fali lat 60 .
