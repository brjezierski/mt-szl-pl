Rolnowo
Rolnowo ( niem .
`` Rollnau '' ) – wieś w Polsce położona w województwie warmińsko-mazurskim , w powiecie ostródzkim , w gminie Morąg .
W latach 1975–1998 miejscowość administracyjnie należała do województwa olsztyńskiego .
Miejscowość leży w historycznym regionie Prus Górnych .
W 1928 r. do wsi przyłączono , jako przysiółki , dwa dawne folwarki Anin ( założony w 1854 ) i Antoniewo ( założony w 1873 ) , wchodzące wcześniej w skład dóbr w Nowym Dworze .
