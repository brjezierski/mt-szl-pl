Roman Kostrzewski
Roman Ryszard Kostrzewski ( ur .
15 lutego 1960 w Piekarach Śląskich , zm .
10 lutego 2022 w Bytomiu ) – polski muzyk , wokalista i autor tekstów .
Najbardziej znany jako wieloletni wokalista zespołu Kat ( w latach 1981–2004 ) .
Współzałożyciel grup Alkatraz ( 2000–2002 ) oraz Kat & amp ; Roman Kostrzewski ( 2004–2022 ) .
Członek Akademii Fonograficznej ZPAV .
Działalność artystyczna .
Pozostałych muzyków Kata spotkał 12 grudnia 1981 roku na pierwszej edycji festiwalu Silesian Rock w Bytomiu .
Wkrótce dołączył do zespołu .
Do wczesnych osiągnięć zespołu Kat zaliczały się m.in .
występ na festiwalu w Jarocinie w 1984 roku oraz wspólna trasa po Polsce z grupą Hanoi Rocks .
W 1985 roku Kat z Kostrzewskim w składzie nagrał pierwszą płytę – `` 666 '' ( oraz jej anglojęzyczną wersję pt .
`` Metal and Hell '' ) .
W maju 1988 nastąpiła premiera nagranego w roku poprzednim albumu `` Oddech wymarłych światów '' , po czym , w wyniku nieporozumień muzyków , Kat zawiesił działalność .
