Roman Kotliński
Roman Kotliński ( ur .
15 marca 1967 w Kole ) – polski wydawca , pisarz , dziennikarz i publicysta oraz polityk , były duchowny rzymskokatolicki .
Był redaktorem naczelnym tygodnika „ Fakty i Mity ” i posłem na Sejm VII kadencji .
Życiorys .
Wykształcenie .
Ukończył liceum ogólnokształcące w Kłodawie .
W latach 1986–1989 kształcił się w Wyższym Seminarium Duchownym we Włocławku .
Po rocznej przerwie kontynuował formację do stanu kapłańskiego w Wyższym Seminarium Duchownym w Łodzi , jednocześnie studiując w łódzkiej filii Akademii Teologii Katolickiej w Warszawie .
W 1993 otrzymał tytuł zawodowy magistra teologii .
Działalność zawodowa .
12 czerwca 1993 przyjął święcenia kapłańskie i został inkardynowany do archidiecezji łódzkiej .
Był wikariuszem w parafiach : Nawiedzenia Najświętszej Maryi Panny w Ruścu , Zesłania Ducha Świętego w Aleksandrowie Łódzkim i Najświętszej Maryi Panny Królowej Polski w Ozorkowie .
Deklaruje , że wiarę stracił w Aleksandrowie po przeczytaniu książki `` Wielki bój '' autorstwa Ellen G. White .
W 2015 decyzją Kongregacji ds .
Duchowieństwa Roman Kotliński został pozbawiony stanu kapłańskiego wraz ze zwolnieniem go od celibatu i wszystkich zobowiązań wynikających ze święceń .
Faktycznie stan kapłański porzucił w 1996 , zawarł następnie związek małżeński i wyjechał na krótko do Kanady .
Po powrocie założył przedsiębiorstwo Glass-Plast , które produkowało przedmioty z tworzyw sztucznych .
Następnie wspólnie z żoną przekształcił posiadane przedsiębiorstwo w wydawnictwo książkowe Niniwa , które wydało m.in .
