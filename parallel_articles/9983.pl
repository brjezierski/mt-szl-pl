Ruchy rewolucyjne kojarzy Zmorski z aktem destrukcji i zburzenia ( koniecznym na drodze historiozoficznej analizy ) , z nihilistycznym gestem unicestwienia okrutnego świata - jednak tylko i wyłącznie w budującym celu utworzenia nowego porządku .
