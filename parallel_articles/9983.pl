Twórczość .
Stylizowana na ludową przyśpiewkę poezja Romana Zmorskiego przedstawia świat pełen frenezji , niepokoju , krwi i rozpaczy − dlatego też zalicza się ją zazwyczaj do nurtu literatury XIX wieku zwanego `` czarnym romantyzmem '' .
Twórczość Romana Zmorskiego − objętościowo dość ograniczona − niemal w całości przynależy do nurtu liryki ludowej polskiego romantyzmu .
Jednocześnie jest ona jednak krańcowo różna od poezji bardziej reprezentatywnych przedstawicieli tego typu literatury takich jak choćby Władysław Syrokomla czy przede wszystkim Teofil Lenartowicz , któremu tak samo patronowała ta sama tradycja Mazowsza ; Zmorski w miejsce idyllicznych obrazków prezentuje wizje krwawe i piekielne , bliskie pełnych frenezji poematów szkoły ukraińskiej polskiego romantyzmu .
Przyrodę i świat duchowy chłopów spaja przy tym ścisły paralelizm , stąd też światem przedstawionym włada skrajny fatalizm i rozpacz , a krajobraz pełen jest wichrów , burz i oznak nadchodzącego `` czegoś strasznego '' .
Najbardziej znanym tego przykładem jest słynna niegdyś ballada `` Dziwy .
Improwizacja '' , w którym ponury nocny plener opowiada historię o morderstwie dokonanym przez żonę na mężu w noc poślubną , a następnie każe duchowi morderczyni na wieczność błąkać się w szaleństwie po pobliskim jeziorze .
Inny wiersz , w podobny sposób wykorzystujący stylistykę ludowej przyśpiewki , `` Wróżby Mazura '' , poprzedza wizję chłopskiej rzezi niepokojącym obrazem nadchodzącego zimowego poranka .
