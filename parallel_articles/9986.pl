Ron Paul
Ron Paul , właśc .
Ronald Ernest Paul ( ur .
20 sierpnia 1935 w Pittsburghu ) – amerykański lekarz i polityk , były członek Izby Reprezentantów z ramienia Partii Republikańskiej z Teksasu , kandydat na prezydenta Stanów Zjednoczonych .
Znany jest ze swoich libertariańskich poglądów .
Jest praktykującym baptystą .
Ojciec Randa Paula .
Życiorys .
Ron Paul urodził się w mieście Pittsburgh w stanie Pensylwania .
W 1957 roku ukończył biologię w Gettysburgu , a w 1961 medycynę na Duke University w Karolinie Północnej .
W latach 60. zajmował się medycyną lotniczą w siłach powietrznych , później prowadził prywatną praktykę lekarską .
Politycznie związany przez większą część kariery z Partią Republikańską .
W 1987 roku opuścił Partię Republikańską by dołączyć do nowej wówczas Partii Libertariańskiej .
W 1988 roku jako kandydat Partii Libertariańskiej , zajął trzecie miejsce ( uzyskując 0,5 % głosów ) w wyborach prezydenckich .
W 1996 ponownie dołączył do partii Republikańskiej .
