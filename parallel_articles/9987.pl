Ronald Reagan
Ronald Wilson Reagan ( ur .
6 lutego 1911 w Tampico , zm .
5 czerwca 2004 , w Los Angeles ) – amerykański polityk , mąż stanu , 40. prezydent Stanów Zjednoczonych oraz 33. gubernator Kalifornii , aktor filmowy .
Jako przywódca USA udzielił znaczącej pomocy ruchom antykomunistycznym , w tym działającej w Polsce „ Solidarności ” .
W czasie jego prezydentury , przeprowadzone zostały reformy w polityce gospodarczej ( tzw .
reaganomika ) o założeniach wywodzących się z ekonomii podaży .
Ukończył Eureka College w 1932 roku i pracował jako komentator radiowy .
Po przeprowadzce do Kalifornii w 1937 r. , znalazł pracę jako aktor filmowy i zagrał w kilku dużych produkcjach oraz był aktywnym członkiem aktorskiego związku zawodowego .
Na początku lat 60. wstąpił do Partii Republikańskiej .
W 1964 roku , jego przemówienie „ Czas na wybieranie ” , zwróciło na niego uwagę całego kraju .
Zdobywając swoich zwolenników , został wybrany na gubernatora Kalifornii ( 1966–1974 ) .
