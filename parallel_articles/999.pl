Ejo
Ejo ( staroż .
`` Aigeion '' , `` Aigion '' ; gr .
`` Αίγιο '' ) – miasto w Grecji , w administracji zdecentralizowanej Peloponez , Grecja Zachodnia i Wyspy Jońskie , w regionie Grecja Zachodnia , w jednostce regionalnej Achaja .
Siedziba gminy Ejalia .
W 2011 roku liczyło 20 422 mieszkańców .
Ośrodek turystyczny .
Jedno z najstarszych miast w Grecji .
Założone w czasach Homera .
