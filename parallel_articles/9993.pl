Ropki
Ropki ( dodatkowa nazwa w j. łemkow .
Рiпкы , trb .
`` Ripky '' ) – wieś w Polsce , położona w województwie małopolskim , w powiecie gorlickim , w gminie Uście Gorlickie .
W latach 1975–1998 wieś administracyjnie należała do województwa nowosądeckiego .
Najwyższy szczyt w pobliżu Ropek to Lackowa ( 997 m n.p.m .
) .
W Ropkach od 1999 istnieje Buddyjski Ośrodek Medytacyjny , Buddyjskiego Związku Diamentowej Drogi Linii Karma Kagyu .
Historia .
Pierwsze wzmianki o wsi pochodzą z 1581 .
Lokowana została na prawie wołoskim a jej właścicielem był Adam Brzeziński .
Pierwsza cerkiew powstała około 1610 .
Prawdopodobnie w 1801 na terenie wsi powstała nowa greckokatolicka cerkiew pod wezwaniem Narodzenia Bogarodzicy ( obecnie znajduje się w Muzeum Budownictwa Ludowego w Sanoku ) .
W Ropkach warto zobaczyć miejsce po cerkwi oraz przylegający do niego cmentarz .
