Roseau
Roseau – stolica , a zarazem największe miasto Wspólnoty Dominiki – wyspiarskiego państwa leżącego w Archipelagu Małych Antyli u ujścia rzeki Roseau do Morza Karaibskiego .
Dawniej nosiło nazwę Charlotte Town .
Ośrodek administracyjny parafii świętego Jerzego .
Miasto stanowi ważny ośrodek przemysłu spożywczego : przetwórstwo owoców , produkcja olejków eterycznych oraz odzieżowego .
Jest tu również duży port rybacki .
Roseau jest centrum turystyczno-wypoczynkowym Dominiki .
Znajduje się w nim między innymi ogród botaniczny oraz muzeum .
Miasto posiada port lotniczy oraz port morski .
